Abençoado que sabe e vê, um arahant, perfeitamente iluminado: ‘[A vida em] qualquer
mundo não tem nada que lhe pertença, todos deixam tudo e prosseguem’. De fato é
assim!<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>41. “Agora,
o Mestre Ratthapala disse: ‘[A vida em] qualquer mundo é incompleta,
insatisfatória, é a escravidão do desejo’. Como deve ser compreendido o
significado dessa afirmação?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O que você
pensa, grande rei? Você governa o rico país dos Kurus?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
Mestre Ratthapala, eu o governo.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O que você
pensa, grande rei? Suponha que um homem confiável e honesto viesse do leste e
dissesse: ‘Por favor, grande rei, saiba que eu vim do leste e lá eu vi um
grande país, poderoso e rico, com muitos habitantes e repleto de gente. Há em
abundância tropas com elefantes, cavalaria, carruagens e infantaria; lá há
marfim em abundância e há abundância de moedas e barras de ouro trabalhadas e
em bruto, e há mulheres em abundância. Com o seu exército atual você poderá
conquistá-lo. Conquiste-o então, grande rei.’ O que você faria?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Nós o
conquistaríamos e governaríamos Mestre Ratthapala.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O que você
pensa, grande rei? Suponha que um homem confiável e honesto viesse do oeste ...
do norte ... do sul e dissesse: ‘Por favor, grande rei, saiba que eu vim do sul
e lá eu vi um grande país, poderoso e rico ... Conquiste-o então, grande rei.’
O que você faria?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Nós o conquistaríamos
e governaríamos Mestre Ratthapala.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Grande
Rei, foi por conta disso que o Abençoado que sabe e vê, um arahant, perfeitamente
iluminado, disse: ‘[A vida em] qualquer mundo é incompleta, insatisfatória, é a
escravidão do desejo’; e quando eu ouvi, vi e compreendi isso, eu deixei a vida
em família e segui a vida santa.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“É
maravilhoso, Mestre Ratthapala, é admirável quão bem isso foi expresso pelo
Abençoado que sabe e vê, um arahant, perfeitamente iluminado: ‘[A vida em] qualquer mundo
é incompleta, insatisfatória, é a escravidão do desejo’. De fato é assim!”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>42. Isso
foi o que o venerável Ratthapala disse. E tendo dito isso ele disse mais:<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Eu vejo homens ricos no mundo, que mesmo assim, <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>devido à ignorância, não dão a sua riqueza acumulada.<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Com cobiça eles entesouram as suas posses<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>desejando ainda mais prazeres sensuais.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Um rei que conquistou a terra através da força<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>e governa sobre um reino circundado pelo oceano<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>ainda insaciado com esta margem do oceano<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>desejando a outra margem também.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>A maioria das outras pessoas também, não somente um rei,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>se deparam com a morte com a cobiça inalterada;<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>[com planos] ainda incompletos elas largam o cadáver;<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>os desejos permanecendo insaciados no mundo.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Os seus parentes lamentam arrancando os cabelos,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>soluçando, ‘Ai de mim! Que pena! Nosso amado está morto!’<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Eles carregam o corpo envolto em mortalhas<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>colocando-o sobre uma pira para queimar.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Envolto numa mortalha, ele deixa a sua riqueza para trás,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>cutucado com paus ele queima [sobre a pira].<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>E ao morrer, nenhum parente ou amigo<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>pode aqui lhe oferecer refúgio ou abrigo.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Enquanto os seus herdeiros tomam a sua riqueza, esse ser<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>tem que prosseguir de acordo com as suas ações;<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>e ao morrer nada pode segui-lo;<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>nem filho, nem esposa, nem riqueza e tampouco propriedades.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>A longevidade não é adquirida com a riqueza<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>nem a prosperidade é capaz de banir a velhice;<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>curta é esta vida, dizem todos os sábios,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>a eternidade não é conhecida, apenas a mudança.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Seja rico ou pobre, ambos sentirão o toque [da Morte],<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>o tolo e o sábio também;<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>mas enquanto o tolo é abatido pela sua tolice,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>nenhum sábio irá sequer tremer com o toque.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Melhor é a sabedoria aqui do que qualquer riqueza,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>visto que através da sabedoria se conquista o objetivo último.<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Pois através da ignorância as pessoas cometem ações ruins<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>enquanto de uma vida para outra fracassam em alcançar o objetivo.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Quando alguém prossegue para o ventre e para o próximo mundo,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>renovando os sucessivos ciclos de renascimento,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>outro com pouca sabedoria, confiando nele,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>também vai para o ventre e para o próximo mundo.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Tal qual um ladrão pego num roubo<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>é submetido ao sofrimento devido à sua má ação,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>assim também, as pessoas após a morte, no próximo mundo,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>são submetidas ao sofrimento por suas más ações.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Os prazeres sensuais, variados, doces, deliciosos,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>de muitos e distintos modos perturbam a mente:<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>vendo o perigo nesses vínculos sensuais<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>eu optei por viver a vida santa, Oh Rei.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Como os frutos caem das árvores, assim também as pessoas,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>tanto jovens como velhas, caem quando este corpo chega ao fim.<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Vendo isso também, Oh Rei, eu segui a vida santa:<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>melhor é o seguro da vida do contemplativo.”<o:p></o:p></span></p>


