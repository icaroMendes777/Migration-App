<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião, o Abençoado estava perambulando pela região dos Kurus
com uma grande Sangha de bhikkhus e por fim ele chegou a uma cidade denominada
Thullakotthita.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. Os
brâmanes chefes de família de Thullakotthita ouviram: “Gotama o contemplativo,
o filho dos Sakyas, que adotou a vida santa deixando o clã dos Sakyas, que
andava perambulando em Kuru com um grande número de bhikkhus chegou em
Thullakotthita. E acerca desse mestre Gotama existe essa boa reputação: ‘Esse
Abençoado </span>é um arahant, perfeitamente iluminado, consumado no
verdadeiro conhecimento e conduta, bem-aventurado, conhecedor dos mundos, um
líder insuperável de pessoas preparadas para serem treinadas, mestre de devas
e humanos, desperto, sublime<span lang=PT-BR style='mso-ansi-language:PT-BR'>.
Ele declara - tendo realizado por si próprio com o conhecimento direto - este mundo
com os seus devas, maras e brahmas, esta população com seus contemplativos e
brâmanes, seus príncipes e povo. Ele ensina o Dhamma com o significado e
fraseado corretos, que é admirável no início, admirável no meio, admirável no
final; e ele revela uma vida santa que é completamente perfeita e imaculada.’ É
bom poder encontrar alguém tão nobre.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. Então,
os brâmanes chefes de família de Thullakotthita se dirigiram ao Abençoado.
Alguns homenagearam o Abençoado e sentaram a um lado; alguns trocaram saudações
corteses com ele e após a troca de saudações sentaram a um lado; alguns
ajuntaram as mãos em respeitosa saudação e sentaram a um lado; alguns
anunciaram o seu nome e clã e sentaram a um lado. Alguns permaneceram em
silêncio e sentaram a um lado. Então, o Abençoado os instruiu, estimulou, motivou e encorajou com um discurso do Dhamma.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. Agora,
naquela ocasião Ratthapala, o filho de um dos principais clãs naquela mesma
Thullakotthita, estava sentado na assembléia. Então, ocorreu-lhe que: “Como eu
entendo o Dhamma ensinado pelo Abençoado, não é fácil viver em família e
praticar a vida santa completamente perfeita, totalmente pura, como uma concha
polida. E se eu raspasse o meu cabelo e barba, vestisse os mantos de cor ocre e
seguisse a vida santa.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. Então,
os brâmanes chefes de família de Thullakotthita, despois de instruídos,
estimulados, motivados e encorajados por um discurso do Dhamma do Abençoado,
ficaram satisfeitos e contentes com as palavras do Abençoado. Então, eles se
levantaram dos seus assentos e depois de homenageá-lo, mantendo-o à sua
direita, partiram. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. Pouco
depois deles terem partido, Ratthapala foi até o Abençoado e depois de
cumprimentá-lo, ele sentou a um lado e disse para o Abençoado: “Venerável
senhor, tal como eu entendo o Dhamma ensinado pelo Abençoado, não é fácil viver
em família e praticar a vida santa completamente perfeita, totalmente pura,
como uma concha polida. Venerável senhor, eu desejo raspar o meu cabelo e
barba, vestir os mantos de cor ocre e seguir a vida santa. Eu receberia a
admissão na vida santa sob o Abençoado e a admissão completa.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Você foi
autorizado pelos seus pais, Ratthapala, para deixar a vida em família e seguir
a vida santa?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
venerável senhor, eu não tenho a autorização dos meus pais.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ratthapala,
os Tathagatas não admitem na vida santa ninguém que não tenha a permissão dos
seus pais.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, eu farei com que<span style="mso-spacerun: yes">  </span>meus pais me
dêem a autorização para deixar a vida em família e seguir a vida santa.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. Então,
Ratthapala levantou do seu assento e depois de homenagear o Abençoado,
mantendo-o à sua direita, partiu. Ele foi até os seus pais e lhes disse: “Mãe e
pai, tal como eu entendo o Dhamma ensinado pelo Abençoado, não é fácil viver em
família e praticar a vida santa completamente perfeita, totalmente pura, como
uma concha polida. Eu desejo raspar o meu cabelo e barba, vestir os mantos de
cor ocre e seguir a vida santa. Dêem-me a sua permissão para que eu deixe a
vida em família e siga a vida santa.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando ele
disse isso, os pais dele responderam: “Querido Ratthapala, você é o nosso único
filho, amado e querido. Você foi educado com conforto, cresceu com conforto;
você nada conhece do sofrimento, querido Ratthapala. Mesmo se você morresse nós
o deixaríamos ir contra nossa vontade, então como poderíamos dar a nossa
permissão para que você deixe a vida em família e siga a vida santa enquanto
você ainda está vivo?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Uma segunda
vez ... Uma terceira vez Ratthapala disse para os seus pais: “Mãe e pai ...
Dêem-me a sua permissão para que eu deixe a vida em família e siga a vida
santa.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Pela terceira
vez os seus pais responderam: “Querido Ratthapala ... como poderíamos dar a
nossa permissão para que você deixe a vida em família e siga a vida santa
enquanto você ainda está vivo?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, por
não ter recebido a permissão dos pais para seguir a vida santa, Ratthapala se
deitou no chão e disse: “Exatamente aqui morrerei ou receberei a admissão na
vida santa.” [<a href="#NA">A</a>]<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><a name=RA></a>8. Então os
pais de Ratthapala disseram:<span style="mso-spacerun: yes">  </span>“Querido
Ratthapala, você é o nosso único filho, amado e querido. Você foi educado com
conforto, cresceu com conforto; você nada conhece do sofrimento, querido
Ratthapala. Levante-se, querido Ratthapala, coma, beba e divirta-se. Ao comer,
beber e se divertir, você poderá ser feliz desfrutando dos prazeres sensuais e
realizar méritos. Nós não permitiremos que você deixe a vida em família e siga
a vida santa. Mesmo se você morresse nós o deixaríamos ir contra nossa vontade,
então como poderíamos dar a nossa permissão para que você deixe a vida em
família e siga a vida santa enquanto você ainda está vivo?” Quando isso foi
dito, Ratthapala permaneceu em silêncio.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Uma segunda
vez ... Uma terceira vez os pais de Ratthapala disseram: “Querido Ratthapala
... como poderíamos dar a nossa permissão para que você deixe a vida em família
e siga a vida santa enquanto você ainda está vivo?” Pela terceira vez<span
style="mso-spacerun: yes">  </span>Ratthapala permaneceu em silêncio.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. Então os
pais de Ratthapala foram até os amigos dele e disseram: “Estimados amigos,
Ratthapala está deitado no chão, depois de dizer: ‘Exatamente aqui morrerei ou
receberei a admissão na vida santa.’ Venham, estimados amigos, vão até
Ratthapala e digam: ‘Amigo Ratthapala, você é o único filho ... Levante-se,
amigo Ratthapala, coma, beba e divirta-se ... como seus pais poderiam dar-lhe a
permissão deles para que você deixe a vida em família e siga a vida santa
enquanto você ainda está vivo?'&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. Então,
os amigos de Ratthapala foram até ele e disseram: “Amigo Ratthapala, você é o
único filho, amado e querido. Você foi educado com conforto, cresceu com
conforto; você nada conhece do sofrimento, amigo Ratthapala. Levante-se, amigo
Ratthapala, coma, beba e divirta-se. Ao comer, beber e se divertir, você poderá
ser feliz desfrutando dos prazeres sensuais e realizar méritos. Os seus pais
não permitirão que você deixe a vida em família e siga a vida santa. Mesmo se
você morresse eles o deixariam ir contra a vontade, então como poderiam eles
dar permissão para que você deixe a vida em família e siga a vida santa
enquanto você ainda está vivo?” Quando isso foi dito, Ratthapala permaneceu em
silêncio.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Uma segunda
vez ... Uma terceira vez os amigos de Ratthapala lhe disseram: “Amigo
Ratthapala ...<span style="mso-spacerun: yes">  </span>como poderiam eles dar
permissão para que você deixe a vida em família e siga a vida santa enquanto
você ainda está vivo?” Pela terceira vez<span style="mso-spacerun: yes"> 
</span>Ratthapala permaneceu em silêncio.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. Então
os amigos de Ratthapala foram até os pais dele e disseram: “Mãe e pai,
Ratthapala está deitado no chão, depois de dizer: ‘Exatamente aqui morrerei ou
receberei a admissão na vida santa.’ Agora, se vocês não derem a permissão para
que ele deixe a vida em família e siga a vida santa, ele irá morrer ali. Mas se
vocês derem a permissão, vocês irão vê-lo depois quando ele estiver seguindo a
vida santa. E se ele não apreciar a vida santa, o que mais poderá ele fazer além
de voltar para cá? Portanto, dêem a ele permissão para que deixe a vida em
família e siga a vida santa.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
estimados amigos, nós damos a nossa permissão para que Ratthapala deixe a vida
em família e siga a vida santa. Mas,<span style="mso-spacerun: yes"> 
</span>seguindo a vida santa, ele terá de nos visitar.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então os
amigos de Ratthapala foram até ele e disseram: “Levante-se, amigo Ratthapala.
Os seus pais deram a permissão para que você deixe a vida em família e siga a
vida santa. Mas, seguindo a vida santa, você terá de visitá-los.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12.
Ratthapala então se levantou e depois de haver recuperado as forças, ele foi
até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse: “Venerável
senhor, eu tenho a permissão dos meus pais para deixar a vida em família e
seguir a vida santa. Que o Abençoado me conceda a admissão na vida santa.”
Então, Ratthapala recebeu a admissão na vida santa sob o Abençoado e a admissão
completa.[<a href="#N1">1</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>13. “Então, não muito tempo depois que o venerável Ratthapala havia
recebido a admissão completa, uma quinzena depois dele ter recebido a admissão
completa, o Abençoado, depois de permanecer em Thullakotthita todo o tempo que
ele quis, saiu perambulando em direção a Savatthi. Caminhando em etapas, ele
por fim chegou em Savatthi e lá ele se instalou no Bosque de Jeta, no Parque de
Anathapindika.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14.
Permanecendo </span>só, isolado, diligente, ardente e decidido, em pouco tempo,
<span lang=PT-BR style='mso-ansi-language:PT-BR'>o venerável Ratthapala, </span>alcançou
e permaneceu no objetivo supremo da vida santa pelo qual membros de um clã deixam
a vida em família pela vida santa, tendo conhecido e realizado por si mesmo no
aqui e agora. Ele soube: “O nascimento foi destruído, a vida santa foi vivida,
o que deveria ser feito foi feito, não há mais vir a ser a nenhum estado.”<span
lang=PT-BR style='mso-ansi-language:PT-BR'>[<a href="#N2">2</a>] <o:p></o:p></span></p>



<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>15. Então, o venerável Ratthapala foi até o Abençoado e depois de
cumprimentá-lo, ele sentou a um lado e disse: “Venerável senhor, desejo visitar
os meus pais, se o Abençoado assim o permitir.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, o
Abençoado penetrou com a sua mente os pensamentos na mente do venerável
Ratthapala. Ao saber que Ratthapala seria incapaz de abandonar o treinamento e
retornar para a vida inferior, ele disse: “Agora é o momento, Ratthapala, faça
como julgar adequado.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16. Então,
o venerável Ratthapala se levantou do seu assento e, depois de homenagear o
Abençoado, mantendo-o à sua direita, partiu. Ele então arrumou o seu lugar de
descanso e tomando a tigela e o manto externo, saiu perambulando em direção a
Thullakotthita. Caminhando em etapas, ele por fim chegou em Thullakotthita e lá
ele se instalou no Parque Migacira do Rei Koravya. Então, ao amanhecer, ele se
vestiu e tomando a tigela e o manto externo foi para Thullakotthita esmolar
alimentos. Enquanto esmolava alimentos de casa em casa em Thullakotthita, ele
acabou chegando na casa do seu pai.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17. Agora,
naquela ocasião o pai do venerável Ratthapala estava sentado na sala principal
penteando o cabelo. Ao ver o venerável Ratthapala vindo à distância, ele disse:
“Nosso único filho, amado e querido, foi convencido a seguir a vida santa por
esses contemplativos carecas.” Então, na casa do seu próprio pai o venerável
Ratthapala nem recebeu comida esmolada e tampouco uma recusa cortês; ao invés
disso, ele apenas recebeu<span style="mso-spacerun: yes">  </span>abusos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18. Foi
quando uma escrava que pertencia aos pais de Ratthapala estava a ponto de jogar
fora um mingau velho.[<a href="#N3">3</a>] <a name=R3></a>Vendo aquilo, o
venerável Ratthapala disse: “Irmã, se isso é para ser jogado fora, então
despeje aqui na minha tigela. Enquanto assim o fazia, ela reconheceu os traços
característicos das suas mãos, dos seus pés e da sua voz. Então ela foi até a
mãe dele e disse: “Minha senhora, saiba que o filho do meu amo, Ratthapala, voltou.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Que bênção!
Se aquilo que você diz for verdade, você não mais será uma escrava!” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então, a
mãe do venerável Ratthapala foi até o pai dele e disse: ‘Chefe de
família, disseram que Ratthapala voltou.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>19.
Justamente naquele momento o venerável Ratthapala estava comendo o mingau velho
junto à parede de um certo abrigo. O pai dele foi até ele e disse: “Ratthapala,
meu querido, com certeza nós ... <span style='mso-bidi-font-weight:bold'>e você
está comendo mingau velho! [<a href="#N4">4</a>] <a name=R4></a>Você não tem a
sua própria casa para onde possa ir?”</span><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Como
poderíamos ter uma casa, chefe de família, quando deixamos a vida em família e
seguimos a vida santa?<span style="mso-spacerun: yes">  </span>Nós não temos
casa, chefe de família. Nós fomos até a sua casa, mas lá não recebemos nem
comida esmolada e tampouco uma recusa cortês; ao invés disso apenas recebemos
abusos.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venha
querido Ratthapala, vamos para casa.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Já basta,
chefe de família, hoje minha refeição está terminada.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
querido Ratthapala, concorde em aceitar a refeição de amanhã.” O venerável
Ratthapala concordou em silêncio.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>20. Então,
sabendo que o venerável Ratthapala havia concordado, o pai dele foi para casa e
fez com que se ajuntasse numa grande pilha, moedas e barras de ouro cobrindo-as
com tapetes. Então, ele disse para as antigas esposas do venerável Ratthapala:
“Venham, noras, enfeitem-se com os ornamentos do jeito que Ratthapala gostava e
achava vocês mais atraentes e desejáveis.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>21. Quando
a noite havia terminado, o pai do venerável Ratthapala fez com que se
preparassem na sua casa vários tipos de boa comida e fez com que se anunciasse
a hora para o venerável Ratthapala: “É hora, querido Ratthapala, a refeição
está pronta.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>22. Então,
depois do amanhecer, o venerável Ratthapala se vestiu e tomando a tigela e o
manto externo foi para a casa do seu pai e sentou num assento que havia sido
preparado. Então, o seu pai fez com que se descobrisse a pilha de moedas e
barras de ouro e disse: “Querido Ratthapala, essa é a sua fortuna maternal; a
sua fortuna paternal é outra e a sua fortuna ancestral é ainda outra. Querido
Ratthapala, você poderá desfrutar da riqueza e realizar méritos. Venha então,
querido, abandone o treinamento e regresse para a vida inferior, desfrute da
riqueza e realize méritos.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Chefe de
família, se você quiser seguir o meu conselho, então faça com que esta pilha de
moedas e barras de ouro seja carregada em carretas para ser despejada no meio
da correnteza do rio Gânges. Por que isso? Porque, chefe de família, por conta
desse ouro irá surgir em você muita tristeza, lamentação, dor, angústia e
