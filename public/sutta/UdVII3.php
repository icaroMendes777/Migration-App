<p class=Normal> Assim, ouvi. Certa ocasião, o Abençoado estava em Savatthi, no Bosque de Jeta, no Parque de Anathapindika. Agora naquela ocasião a maioria das pessoas em Savatthi estavam apegadas excessivamente aos prazeres sensuais. Elas viviam cobiçando, desejando, atadas, apaixonadas, viciadas, embriagadas pelos prazeres sensuais.</p>

<p class=Normal>Então, ao amanhecer, um grande número de bhikkhus se vestiram e tomando a tigela e o manto externo, foram para Savatthi para esmolar alimentos. Depois de haver esmolado em Savatthi e de haver retornado, após a refeição eles foram até o Abençoado e depois de cumprimentá-lo sentaram a um lado e disseram: “Venerável senhor, aqui em Savatthi, a maioria das pessoas em Savatthi estão apegadas excessivamente aos prazeres sensuais. Elas vivem cobiçando, desejando, atadas, apaixonadas, viciadas, embriagadas pelos prazeres sensuais."
</p>

<p class=Normal>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p>

<p class=Normal style='margin-left:.75in'> Apegados aos prazeres sensuais, aos vínculos sensuais,<br>
não vendo nos grilhões nada censurável,<br>
nunca aqueles agrilhoados <br>
cruzarão a torrente tão ampla e imensa.</p>

