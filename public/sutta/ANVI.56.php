<p class=Normal> Numa certa ocasião o Venerável Phagguna estava atormentado, sofrendo e gravemente enfermo. Então o Venerável Ananda foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse: “Venerável senhor, o Venerável Phagguna está atormentado, sofrendo e gravemente enfermo. Seria bom se o Abençoado visitasse o Venerável Phagguna, por compaixão.” O Abençoado concordou em silêncio. </p>

<p class=Normal> Então ao anoitecer, o Abençoado saiu do seu isolamento e foi até o Venerável Phagguna. O Venerável Phagguna viu o Abençoado vindo à distância e ficou agitado na cama. O Abençoado lhe disse: "Já basta Phagguna, não fique agitado. Há estes assentos que foram preparados, eu me sentarei ali." </p>

<p class=Normal> O Abençoado sentou e disse para o Venerável Phagguna: "Eu espero que você esteja melhorando, Phagguna, espero que você esteja confortável, espero que as suas sensações de dor estejam diminuindo e não aumentando e que a sua diminuição, não o seu aumento, seja evidente." </p>

<p class=Normal> "Venerável senhor, eu não estou melhorando, não me sinto confortável. Minhas sensações de dor estão aumentando, não diminuindo, o seu aumento, não a sua diminuição é evidente. Tal como se um homem forte estivesse partindo a minha cabeça com uma espada afiada, da mesma forma, ventos violentos atravessam a minha cabeça. Eu não estou melhorando ... Tal como se um homem forte estivesse apertando uma correia de couro dura em volta da minha cabeça, da mesma forma, sinto dores violentas na minha cabeça. Eu não estou melhorando ... Tal como se um açougueiro habilidoso ou seu aprendiz cortassem o ventre de um boi com uma faca de açougueiro afiada, da mesma forma, ventos violentos estão cortando o meu ventre. Eu não estou melhorando ... Tal como se dois homens fortes agarrassem um homem mais fraco por ambos braços e o assassem sobre uma cova com carvão em brasa, da mesma forma, um fogo violento queima no meu corpo. Eu não estou melhorando, não me sinto confortável. Minhas sensações de dor estão aumentando, não diminuindo, o seu aumento, não a sua diminuição é evidente." </p>

<p class=Normal> Então o Abençoado instruiu, motivou, estimulou e encorajou o Venerável Phagguna com um discurso do Dhamma, depois do que, ele levantou do seu assento. Pouco tempo depois que ele havia partido, o Venerável Phagguna faleceu. No momento da sua morte as suas faculdades estavam serenas. </p>

<p class=Normal> Então o Venerável Ananda foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse: "Venerável senhor, pouco tempo depois que o Abençoado partiu, o Venerável Phagguna faleceu. No momento da sua morte as suas faculdades estavam serenas." </p>

<p class=Normal> "Por que Ananda, as faculdades do bhikkhu Phagguna não deveriam estar serenas? Embora a mente dele ainda não estivesse libertada dos cinco primeiros grilhões, ao ouvir aquele discurso do Dhamma a sua mente se libertou deles. [<a href="#N1">1</a>] </p>

<p class=Normal><a name=R1></a> "Ananda, há esses seis benefícios em ouvir o Dhamma no momento apropriado e examinar o significado no momento apropriado. Quais seis? </p>

<p class=Normal> (1) "Aqui, Ananda, a mente de um bhikkhu ainda não está libertada dos cinco primeiros grilhões, mas no momento da morte ele consegue ver o Tathagata. O Tathagata lhe ensina o Dhamma que é admirável no início, admirável no meio, admirável no final com o correto fraseado e significado; ele revela a vida santa que é perfeita e imaculada. Quando o bhikkhu ouve esse discurso do Dhamma, a sua mente é libertada dos cinco primeiros grilhões. Esse é o primeiro benefício em ouvir o Dhamma no momento apropriado. </p>

<p class=Normal> (2) "Outra vez, Ananda, a mente de um bhikkhu ainda não está libertada dos cinco primeiros grilhões. No momento da morte ele não vê o Tathagata, mas ele consegue ver um discípulo do Tathagata. O discípulo do Tathagata lhe ensina o Dhamma ... ele revela a vida santa que é perfeita e imaculada. Quando o bhikkhu ouve esse discurso do Dhamma, a sua mente é libertada dos cinco primeiros grilhões. Esse é o segundo benefício em ouvir o Dhamma no momento apropriado. </p>

<p class=Normal> (3) "Outra vez, Ananda, a mente de um bhikkhu ainda não está libertada dos cinco primeiros grilhões. No momento da morte ele não vê o Tathagata ou um discípulo do Tathagata, mas ele pensa, examina e investiga o Dhamma tal como ele ouviu e aprendeu. Ao fazer isso a sua mente é libertada dos cinco primeiros grilhões. Esse é o terceiro benefício em examinar o significado no momento apropriado. </p>

<p class=Normal><a name=R2></a> (4) "Aqui, Ananda, a mente de um bhikkhu está libertada dos cinco primeiros grilhões mas ainda não está libertada na insuperável extinção de todas aquisições. [<a href="#N2">2</a>] No momento da morte ele consegue ver o Tathagata. O Tathagata lhe ensina o Dhamma ... revela a vida santa que é perfeita e imaculada. Quando o bhikkhu ouve esse discurso do Dhamma, a sua mente é libertada na insuperável extinção de todas aquisições. Esse é o quarto benefício em ouvir o Dhamma no momento apropriado. </p>

<p class=Normal> (5) "Outra vez, Ananda, a mente de um bhikkhu está libertada dos cinco primeiros grilhões mas ainda não está libertada na insuperável extinção de todas aquisições. No momento da morte ele não vê o Tathagata, mas ele consegue ver um discípulo do Tathagata. O discípulo do Tathagata lhe ensina o Dhamma ... ele revela a vida santa que é perfeita e imaculada. Quando o bhikkhu ouve esse discurso do Dhamma, a sua mente é libertada na insuperável extinção de todas aquisições. Esse é o quinto benefício em ouvir o Dhamma no momento apropriado. </p>

<p class=Normal> (6) "Outra vez, Ananda, a mente de um bhikkhu está libertada dos cinco primeiros grilhões mas ainda não está libertada na insuperável extinção de todas aquisições. No momento da morte ele não vê o Tathagata ou um discípulo do Tathagata, mas ele pensa, examina e investiga o Dhamma tal como ele ouviu e aprendeu. Ao fazer isso a sua mente é libertada na insuperável extinção de todas aquisições. Esse é o sexto benefício em examinar o significado no momento apropriado. </p>

<p class=Normal> "Esses, Ananda, são os seis benefícios em ouvir o Dhamma no momento apropriado e examinar o significado no momento apropriado." </p>


