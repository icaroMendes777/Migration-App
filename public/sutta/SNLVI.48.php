<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
suponham que este grande planeta terra estivesse completamente coberto com água
e um homem lançasse uma bóia com um único furo. Um vento do leste a empurraria
para o oeste, um vento do oeste a empurraria para o leste. Um vento do norte a
empurraria para o sul, um vento do sul a empurraria para o norte. E suponham
que houvesse uma tartaruga cega. Ela viria para a superfície uma vez a cada cem
anos. Agora o que vocês pensam, bhikkhus, aquela tartaruga cega, vindo para a
superfície uma vez a cada cem anos, colocaria o seu pescoço naquela bóia com um
único furo?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Seria por mero acaso,
venerável senhor, que a tartaruga cega, vindo para a superfície uma vez a cada
cem anos, colocasse o seu pescoço naquela bóia com um único furo.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Assim também, é um mero
acaso que alguém obtenha o estado humano. [</span><a href="#N1"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>1</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>] <a name=R1></a><span
style='mso-spacerun:yes'> </span>Assim também, é um mero acaso que um
Tathagata, um Arahant, perfeitamente iluminado, surja no mundo. Do mesmo modo,
é um mero acaso que o Dhamma e Disciplina expostos por um Tathagata brilhe no
mundo. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Vocês obtiveram esse
estado humano, bhikkhus. Um Tathagata, um Arahant, perfeitamente iluminado,
surgiu no mundo. O Dhamma e Disciplina expostos pelo Tathagata brilha no mundo.
<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto, bhikkhus, um
esforço deve ser feito para compreender: ‘Isto é sofrimento.’ Um esforço deve
ser feito para compreender: ‘Esta é a origem do sofrimento.’ Um esforço deve
ser feito para compreender: ‘Esta é a cessação do sofrimento.’ Um esforço deve
ser feito para compreender: ‘Este é o caminho que conduz à cessação do
sofrimento.’”<o:p></o:p></span></p>

