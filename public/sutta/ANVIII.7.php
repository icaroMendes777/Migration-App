<p class=Normal><a name=R1></a>Certa ocasião o Abençoado estava em Rajagaha na montanha do pico do abutre não muito tempo depois que Devadatta havia partido. [<a href="#N1">1</a>] Lá ele se dirigiu aos bhikkhus com referência a Devadatta: </p>

<p class=Normal>"Bhikkhus, é bom que de tempos em tempos um bhikkhu revise os seus próprios fracassos. É bom que de tempos em tempos ele revise os fracassos dos outros. É bom que de tempos em tempos ele revise os seus próprios êxitos. É bom que de tempos em tempos ele revise os êxitos dos outros. Por estar tomado e obcecado por oito condições ruins, Devadatta está destinado para os planos inferiores, destinado ao inferno, e ele ali permanecerá por um eon, irremível. Quais oito? </p>

<p class=Normal> (1) Por estar tomado e obcecado pelo ganho, Devadatta está destinado para os planos inferiores, destinado ao inferno, e ele ali permanecerá por um eon, irremível. (2) Por estar tomado e obcecado pela perda ...  (3) Por estar tomado e obcecado pela fama ... (4) Por estar tomado e obcecado pela má reputação ... (5) Por estar tomado e obcecado pela honra ... (6) Por estar tomado e obcecado pela desonra ... (7) Por estar tomado e obcecado pelos desejos ruins ... (8) Por estar tomado e obcecado pelas amizades ruins, Devadatta está destinado para os planos inferiores, destinado ao inferno, e ele ali permanecerá por um eon, irremível. Por estar tomado e obcecado por essas oito condições ruins, Devadatta está destinado para os planos inferiores, destinado ao inferno, e ele ali permanecerá por um eon, irremível. </p>

<p class=Normal>"É bom para um bhikkhu superar o ganho onde quer que este surja. É bom para ele superar a perda onde quer que esta surja ... superar a fama ... a má reputação ... a honra ... a desonra ... os desejos ruins ... as amizades ruins onde quer que estas surjam. </p>

<p class=Normal>"E por qual razão um bhikkhu deveria superar o ganho onde quer que este surja? Por qual razão ele deveria superar a perda ... a fama ... a má reputação ... a honra ... a desonra ... os desejos ruins ... as amizades ruins onde quer que estas surjam? Aquelas impurezas, aflitivas e febris, que podem surgir naquele que não superou o ganho que surgiu, não ocorrem naquele que superou o ganho. Aquelas impurezas, aflitivas e febris, que podem surgir naquele que não superou a perda ... a fama ... a má reputação ... a honra ... a desonra ... os desejos ruins ... as amizades ruins que podem surgir naquele que não superou as amizades ruins que surgiram, não ocorrem naquele que superou as amizades ruins. Por essa razão um bhikkhu deve abandonar o ganho sempre que este surgir. Ele deve abandonar a perda ... a fama ... a má reputação ... a honra ... a desonra ... os desejos ruins ... as amizades ruins sempre que estas surgirem. </p>

<p class=Normal>"Portanto, bhikkhus, assim vocês devem treinar: 'Nós superaremos o ganho sempre que este surgir. Nós superaremos a perda ... a fama ... a má reputação ... a honra ... a desonra ... os desejos ruins ... as amizades ruins sempre que estas surgirem.' Assim é que vocês devem treinar." </p>

