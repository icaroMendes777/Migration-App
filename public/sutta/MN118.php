<p class=Normal><a name=T24></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>24.
&quot;Bhikkhus,<span style='mso-spacerun:yes'>  </span>sempre que um bhikkhu,
inspirando longo, compreende: ‘eu inspiro <span class=GramE>longo,</span>’ ou
