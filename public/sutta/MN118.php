bhikkhus <span class=SpellE>sêniores</span> alcançaram sucessivos estágios de
distinção.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. Naquela ocasião – o
décimo quinto dia do Uposatha, a noite de lua cheia do quarto mês do <span
class=SpellE>Komudi</span> – o Abençoado estava sentado ao ar livre circundado
pela Sangha de bhikkhus. Então observando a silenciosa Sangha de bhikkhus ele
falou:<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. &quot;Bhikkhus esta
Sangha está livre da fala inconseqüente, esta Sangha está livre da tagarelice.
Ela é sólida como o cerne. Assim é esta<span style='mso-spacerun:yes'> 
</span>Sangha dos bhikkhus, assim é esta assembléia.<span
style='mso-spacerun:yes'>  </span>Uma assembléia como essa é merecedora de
dádivas, merecedora de hospitalidade, merecedora de oferendas, merecedora de
saudações com reverência, um campo inigualável de mérito para o mundo, assim é
esta Sangha dos bhikkhus, assim é esta assembléia. Esta assembléia é uma
comunidade tal que uma pequena oferenda se transforma num grande fruto e uma
grande oferenda se transforma num fruto ainda maior – assim é esta Sangha de
bhikkhus, assim é esta assembléia. Uma assembléia assim é rara, <span
class=GramE>difícil de se</span> encontrar neste mundo – assim é essa Sangha de
bhikkhus, assim é esta assembléia. Por uma assembléia assim, valeria a pena uma
longa viagem com um grande carregamento de alimentos para poder vê-la – assim é
esta Sangha de bhikkhus, assim é esta assembléia. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. <span class=GramE>&quot;Nesta
Sangha de bhikkhus existem</span> bhikkhus que são arahants, livres das
impurezas e que têm vivido a vida santa, fizeram o que deve ser feito,
depuseram o fardo, alcançaram o verdadeiro objetivo, destruíram os grilhões da
existência e estão completamente libertados através do conhecimento supremo –
esses bhikkhus estão aqui nesta Sangha de bhikkhus.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. <span class=GramE>&quot;Nesta
Sangha de bhikkhus existem</span> bhikkhus que, com a destruição dos cinco
primeiros grilhões, devem renascer espontaneamente (nas Moradas Puras) e lá
realizar o parinibbana, sem<span style='mso-spacerun:yes'>  </span>nunca mais retornar
desse mundo – esses bhikkhus estão aqui nesta Sangha de bhikkhus.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. <span class=GramE>&quot;Nesta
Sangha de bhikkhus existem</span> bhikkhus que com a destruição dos três
grilhões e com a atenuação da cobiça, raiva e delusão, são os que “retornarão
uma vez”, retornando uma vez a esse mundo para dar fim ao sofrimento – tais
bhikkhus estão aqui nesta Sangha de bhikkhus. <span
style='mso-spacerun:yes'>      </span><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. <span class=GramE>&quot;Nesta
Sangha de bhikkhus existem</span> bhikkhus que<span style='mso-spacerun:yes'> 
