poderes ... dos sete fatores para a iluminação ... do Nobre Caminho Óctuplo –
tais bhikkhus estão aqui nessa Sangha de bhikkhus.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14. </span><span
