</span>Plena<span style='mso-spacerun:yes'>  </span>na Respiração) </b><o:p></o:p></span></p>

<p class=Normal><a name=T15></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. <span
class=GramE>&quot;Bhikkhus, a atenção plena na respiração quando desenvolvida e
