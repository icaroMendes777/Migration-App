não mais destinados aos mundos inferiores, com o destino fixo, eles têm a
iluminação como destino – tais bhikkhus estão aqui nessa Sangha de bhikkhus. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. &quot;Nesta Sangha de
