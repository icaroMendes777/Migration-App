<p class=Normal> “Bhikkhus, há esses cinco poderes de um treinando.[<a href="#N1">1</a>] <a name="R1"> </a>Quais cinco? O poder da convicção, o poder da vergonha de cometer transgressões, o poder do temor de cometer transgressões, o poder da energia e o poder da sabedoria. </p>

<p class=Normal> “E qual é o poder da convicção? É o caso em que um bhikkhu, um nobre discípulo, deposita fé na iluminação do Tathagata assim: ‘O Abençoado é um arahant, perfeitamente iluminado, consumado no verdadeiro conhecimento e conduta, bem-aventurado, conhecedor dos mundos, um líder insuperável de pessoas preparadas para serem treinadas, mestre de devas e humanos, desperto, sublime.’ Isso, bhikkhus, é chamado o poder da convicção. </p>

<p class=Normal> “E qual é o poder da vergonha de cometer transgressões? [<a href="#N2">2</a>] <a name="R2"> </a>É o caso em que um bhikkhu, um nobre discípulo, tem senso da vergonha; ele sente vergonha da conduta corporal imprópria, da conduta verbal imprópria e da conduta mental imprópria. Isso, bhikkhus, é chamado o poder da vergonha. </p>

<p class=Normal> “E qual é o poder do temor de cometer transgressões? É o caso em que um bhikkhu, um nobre discípulo, tem senso do temor; ele teme a conduta corporal imprópria, a conduta verbal imprópria e a conduta mental imprópria. Isso, bhikkhus, é chamado o poder do temor. </p>

<p class=Normal> “E qual é o poder da energia? É o caso em que um bhikkhu, um nobre discípulo, permanece com a sua energia estimulada para o abandono de qualidades mentais prejudiciais e para a obtenção de qualidades mentais benéficas. Ele é decidido, firme no seu esforço, sem se esquivar das suas responsabilidades com relação ao cultivo de qualidades mentais benéficas. Isso, bhikkhus, é chamado o poder da energia. </p>

<p class=Normal> “E qual é o poder da sabedoria? É o caso em que um bhikkhu, um nobre discípulo, tem sabedoria, com a completa compreensão da origem e cessação - nobre, penetrante, que conduz ao fim do sofrimento. Isso, bhikkhus, é chamado o poder da sabedoria. </p>

<p class=Normal> “Esses, bhikkhus, são os cinco poderes de um treinando. Assim vocês deveriam treinar: ‘Nós obteremos o poder da convicção de um treinando, o poder da vergonha de um treinando, o poder do temor de um treinando, o poder da energia de um treinando, o poder da sabedoria de um treinando.’ Assim é como vocês deveriam treinar.” </p>

