<p class=Normal>Então o venerável Ananda foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse: “Venerável senhor, um bhikkhu poderia alcançar uma tal concentração de modo que ele não teria a percepção da terra em relação à terra, nem da água em relação à água, nem do fogo … do ar … da base do espaço infinito ... da base da consciência infinita ... da base do nada ... da base da nem percepção, nem não percepção ... deste mundo ... nem do mundo além em relação ao mundo além, e ainda assim seria perceptivo?” </p>

<p class=Normal> “Sim, Ananda, um bhikkhu poderia …”</p>

<p class=Normal> “Mas como, venerável senhor, um bhikkhu poderia alcançar uma tal concentração  de modo que ele não teria a percepção da terra em relação à terra ... nem do mundo além, e ainda assim seria perceptivo?” </p>

<p class=Normal> “É o caso, Ananda, em que um bhikkhu tem a percepção deste modo: ‘Isto é a paz, isto é o sublime, isto é, o silenciar de todas as formações, o abandono de todas as aquisições, a destruição do desejo, desapego, cessação, nibbana.’ É desse modo que um bhikkhu pode alcançar uma tal concentração  de modo que ele não tenha a percepção da terra em relação à terra, nem da água em relação à água, nem do fogo … do ar … da base do espaço infinito ... da base da consciência infinita ... da base do nada ... da base da nem percepção, nem não percepção ... deste mundo ... nem do mundo além em relação ao mundo além, e ainda assim ser perceptivo.” </p>

