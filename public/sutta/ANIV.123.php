<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
há esses quatro tipos de indivíduos que podem ser encontrados no mundo. Quais
quatro?<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Há o caso
em que um indivíduo, afastado dos prazeres sensuais, afastado das qualidades
não hábeis, entra e permanece no primeiro jhana, que é caracterizado pelo
pensamento aplicado e sustentado, com o êxtase e felicidade nascidos do
afastamento. Ele desfruta disso, deseja isso, encontra satisfação através
disso. Permanecendo nisso – fixado nisso, permanecendo nisso freqüentemente,
não abandonando isso – então, ao morrer ele renasce no mundo dos devas do cortejo
de Brahma. Os devas do cortejo de Brahma, bhikkhus, têm um tempo de vida de um
éon. Uma pessoa comum tendo estado ali, tendo usado o seu tempo de vida, vai para o inferno, o ventre animal ou para o mundo dos
fantasmas famintos. Mas um discípulo do Abençoado, tendo estado ali, tendo
usado o seu tempo de vida, estará libertado naquela mesma
existência. Essa, bhikkhus, é a diferença, a distinção, o fator que distingue,
um nobre discípulo instruído de uma pessoa comum sem instrução - o seu destino,
o seu<span style='mso-spacerun:yes'>  </span>renascimento. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Novamente,
há o caso em que um indivíduo, abandonando o pensamento aplicado e sustentado, entra
e permanece no segundo jhana, que é caracterizado pela segurança interna e
perfeita unicidade da mente, sem o pensamento aplicado e sustentado, com o êxtase
e felicidade nascidos da concentração. Ele desfruta disso, deseja isso,
encontra satisfação através disso. Permanecendo nisso – fixado nisso,
permanecendo nisso freqüentemente, não abandonando isso – então, ao morrer ele renasce
no mundo dos devas do Abhassara. Os devas do Abhasara, bhikkhus, têm um tempo
de vida de dois éons. Uma pessoa comum tendo estado ali, tendo usado o seu tempo de vida, vai para o inferno, o ventre animal ou para o
mundo dos fantasmas famintos. Mas um discípulo do Abençoado, tendo estado ali,
tendo usado o seu tempo de vida, estará libertado naquela mesma
existência. Essa, bhikkhus, é a diferença, a distinção, o fator que distingue,
um nobre discípulo instruído de uma pessoa comum sem instrução - o seu destino,
o seu<span style='mso-spacerun:yes'>  </span>renascimento.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Novamente,
há o caso em que um indivíduo, abandonando o êxtase, entra e permanece no
terceiro jhana que é caracterizado pela felicidade sem o êxtase, acompanhada
pela atenção plena, plena consciência e equanimidade, acerca do qual os nobres
declaram: ‘Ele permanece numa estada feliz, equânime e plenamente atento.’ Ele
desfruta disso, deseja isso, encontra satisfação através disso. Permanecendo
nisso – fixado nisso, permanecendo nisso freqüentemente, não abandonando isso –
então, ao morrer ele renasce no mundo dos devas do Subhakinha. Os devas do Subhakinha,
bhikkhus, têm um tempo de vida de quatro éons. Uma pessoa comum tendo estado
ali, tendo usado o seu tempo de vida, vai para o inferno, o
ventre animal ou para o mundo dos fantasmas famintos. Mas um discípulo do
Abençoado, tendo estado ali, tendo usado o seu tempo de vida, estará
libertado naquela mesma existência. Essa, bhikkhus, é a diferença, a distinção,
o fator que distingue, um nobre discípulo instruído de uma pessoa comum sem
instrução - o seu destino, o seu<span style='mso-spacerun:yes'> 
</span>renascimento.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Novamente,
há o caso em que um indivíduo, com o completo desaparecimento da felicidade, entra
e permanece no quarto jhana, que possui nem felicidade, nem sofrimento, com a
atenção plena e a equanimidade purificadas. Ele desfruta disso, deseja isso,
encontra satisfação através disso. Permanecendo nisso – fixado nisso,
permanecendo nisso freqüentemente, não abandonando isso – então, ao morrer ele renasce
no mundo dos devas do Vehapphala. Os devas do Vehapphala, bhikkhus, têm um
tempo de vida de 500 éons. Uma pessoa comum tendo estado ali, tendo usado o seu tempo de vida, vai para o inferno, o ventre animal ou para o
mundo dos fantasmas famintos. Mas um discípulo do Abençoado, tendo estado ali,
tendo usado o seu tempo de vida, estará libertado naquela mesma
existência. Essa, bhikkhus, é a diferença, a distinção, o fator que distingue,
um nobre discípulo instruído de uma pessoa comum sem instrução - o seu destino,
o seu<span style='mso-spacerun:yes'>  </span>renascimento.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Esses são
os quatro tipos de indivíduos que podem ser encontrados no mundo.”<o:p></o:p></span></p>

