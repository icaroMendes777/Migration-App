<p class=Normal> "Bhikkhus, há esses seis casos de incapacidade. Quais seis? Alguém dotado de entendimento correto é (1) incapaz de basear-se na idéia que o prazer e a dor são criados pela própria pessoa; (2) incapaz de basear-se na idéia que o prazer e a dor são criados por outros; (3) incapaz de basear-se na idéia que o prazer e a dor são criados pela própria pessoa e por outros; (4) incapaz de basear-se na idéia que o prazer e a dor não são criados pela própria pessoa mas surgem fortuitamente; (5) incapaz de basear-se na idéia que o prazer e a dor não são criados por outros mas surgem fortuitamente; (6) incapaz de basear-se na idéia que o prazer e a dor não são criados nem pela própria pessoa nem são criados por outros mas surgem fortuitamente. Por qual razão? Porque a pessoa dotada de entendimento correto viu  com clareza a condicionalidade e os fenômenos que surgem com base nas condições. Esses são os seis casos de incapacidade." </p>


