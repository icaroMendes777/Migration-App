<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como,
bhikkhus, um bhikkhu tem atenção plena? Neste caso, bhikkhus, um bhikkhu
permanece contemplando o corpo como um corpo, ardente, plenamente consciente e
com atenção plena, tendo colocado de lado a cobiça e o desprazer pelo mundo.
Ele permanece contemplando as sensações como sensações ... mente como mente ...
objetos mentais como objetos mentais, ardente, plenamente consciente e com
atenção plena, tendo colocado de lado a cobiça e o desprazer pelo mundo. É desse
modo, bhikkhus, que um bhikkhu tem atenção plena.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como,
bhikkhus, um bhikkhu tem plena consciência? Neste caso, bhikkhus, para um
bhikkhu as sensações são compreendidas quando surgem, compreendidas enquanto
estão presentes, compreendidas quando desaparecem. Os objetos mentais são
compreendidos quando surgem, compreendidos enquanto estão presentes,
compreendidos quando desaparecem. As percepções são compreendidas quando
surgem, compreendidas enquanto estão presentes, compreendidas quando desaparecem.
É desse modo bhikkhus, que um bhikkhu exerce a plena consciência.[<a href="#N1">1</a>]<o:p></o:p></span></p>


