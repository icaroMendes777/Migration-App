<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. [</span><a href="#N1"><span lang=PT-BR style='mso-ansi-language:PT-BR'>1</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R1></a>Em certa ocasião o
Abençoado estava em Rajagaha, em Isigili – a Garganta dos Profetas. Lá ele se
dirigiu aos monges desta forma: “Bhikkhus” – “Venerável Senhor,” eles
responderam. O Abençoado disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. “Vocês
vêem, bhikkhus, não vêem, aquela montanha Vebhara?”[<a href="#N2">2</a>] <a
name=R2></a><span style="mso-spacerun: yes"> </span>- “Sim, venerável senhor.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Havia um
outro nome, outra designação, para essa montanha Vebhara. Vocês vêem, bhikkhus,
não vêem, aquela montanha Pandava?” - “Sim, venerável senhor.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Havia um
outro nome, outra designação, para essa montanha Pandava. Vocês vêem, bhikkhus,
não vêem, aquela montanha Vepulla?” - “Sim, venerável senhor.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Havia um
outro nome, outra designação, para essa montanha Vepulla. Vocês vêem, bhikkhus,
não vêem, aquela montanha Gijjhakuta – o Pico do Abutre?” - “Sim, venerável
senhor.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Havia um
outro nome, outra designação, para essa montanha Gijjhakuta - o Pico do Abutre.
Vocês vêem, bhikkhus, não vêem, aquela montanha Isigili - a Garganta dos
Profetas?” - “Sim, venerável senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. “Havia
um outro nome, outra designação, para essa montanha Isigili - a Garganta dos
Profetas. Pois em tempos passados quinhentos paccekabuddhas [<a href="#N3">3</a>]
<a name=R3></a>viveram por muito tempo em Isigili, a Garganta dos Profetas.
Eles foram vistos subindo essa montanha e uma vez lá encima, eles não mais
foram vistos. As pessoas que viram aquilo disseram: ‘Essa montanha engole os
profetas.’ E assim foi que este lugar passou a ser chamado ‘A Garganta dos
Profetas.’ Eu lhes direi, bhikkhus, os nomes dos paccekabuddhas, eu relatarei
os nomes dos paccekabuddhas, eu ensinarei para vocês os nomes dos
paccekabuddhas. Ouçam e prestem muita atenção ao que vou dizer.” – “Sim,
venerável senhor,” os bhikkhus responderam. O Abençoado disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4.
“Bhikkhus, o paccekabuddha Arittha viveu por muito tempo nesta montanha
Isigili. O paccekabuddha Uparittha viveu por muito tempo nesta montanha
Isigili. O paccekabuddha Tagarasikhin [<a href="#N4">4</a>] ... <a name=R4></a>Yasassin
... Sudassana ... Piyadassin ... Gandhara ... Pindola ... Upasabha ... Nitha
... Tatha ...Sutava ... Bhavitatta viveu por muito tempo nesta montanha
Isigili.<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>5. “Esses seres santificados, desprovidos do desejo, livres do
sofrimento, <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>realizaram o despertar por si mesmos - <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>ouçam o meu relato dos nomes daqueles, os mais sublimes<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>dentre os homens, que arrancaram a flecha [da dor].<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Arittha, Uparittha, Tagarasikhin, Yasassin,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Sudassana, e Piyadassin o iluminado,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Gandhara, Pindola, Upasabha também,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Nitha, Tatha, Sutava, Bhavitatta. <o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>6. “Sumbha, Subha, Methula e Atthama<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>depois Assumegha, Anigha, Sudatha -<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>e Hingu e Hinga, o muito poderoso, <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>paccekabuddhas não mais conduzidos ao devir.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Dois sábios com os nomes Jali e Atthaka,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>depois Kosala o iluminado, depois Subahu,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Upanemi, Nemi e Santacitta <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>íntegro e verdadeiro, imaculado e sábio.<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Kala, Upakala, Vijita e Jita;<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Anga e Panga e Gutijjita também;<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Passin conquistou o apego, a raiz do sofrimento; <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Aparajita conquistou os poderes de Mara.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Satthar, Pavattar, Sarabhanga, Lomahamsa,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Uccangamaya, Asita, Anasava,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Manomaya e Bandhumant livres do orgulho, <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Tadadhimutta imaculado e resplandecente;<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Ketumbaraga, Matanga e Ariya,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>depois Accuta, Accutagama, Byamaka,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Sumangala, Dabbila, Supatitthita,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Asayha, Khemabhirata, e Sorata,<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Durannaya, Sangha e depois Ujjaya;<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>outro sábio, Sayha, com o nobre empenho.<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>E doze entre - Anandas, Nandas e Upanandas – <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>e Bharadvaja suportando seu último corpo;<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Depois Bodhi, Mahanama o supremo,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Bharadvaja com o cabelo longo e claro;<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Tissa e Upatissa não destinados ao devir;<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Upasidarin e Sidarin, livres do desejo.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Iluminado era Mangala, livre da cobiça;<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Usabha cortou a rede, a raiz do sofrimento.<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Upanita alcançou o estado de paz,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>purificado, excelente, verdadeiro.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Jeta, Jayanta, Paduma e Uppala,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Padumuttara, Rakkhita e Pabbata, <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Manatthaddha glorioso, Vitaraga<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>e Kanha iluminados com a mente libertada.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>7. “Esses e também outros eminentes e poderosos<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>paccekabuddhas não mais destinados ao devir - <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>honra a esses sábios que, transcendendo o desejo,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>realizaram o parinibbana, acima de qualquer limite.”<o:p></o:p></span></p>


