<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[1] De acordo com MA, o ven. Bakkula se tornou
monge aos oitenta anos, sendo que ele teria 160 anos na época deste sutta. Ele
foi declarado pelo Buda como sendo o discípulo mais destacado em<span
style="mso-spacerun: yes">  </span>relação à boa saúde. [<a href="#R1">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[2] MA diz que o trecho que a seguir se
encontra entre colchetes foi acrescentado pelos bhikkhus sêniores que
compilaram o Dhamma. [<a href="#R2">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[3]<span style="mso-spacerun: yes"> 
</span>Este trecho e aqueles que seguem mostram o ven. Bakkula como um que
segue as práticas ascéticas. Kathina é o dia, após o retiro das chuvas de
três meses, no qual os bhikkhus confeccionam novos mantos com os tecidos que
receberam. [<a href="#R3">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[4] MA diz que depois dele ter seguido a vida
santa, ele continuou sendo uma pessoa comum durante sete dias mas no oitavo dia
ele realizou o estado de arahant junto com os conhecimentos analíticos (<i>patisambhida</i>).
[<a href="#R4">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N5></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[5] MA: O ven. Bakkula mesmo não lhe conferiu
a ordenação, (o que seria uma violação do seu tipo de prática), mas ele fez com
que outros bhikkhus a dessem. [<a href="#R5">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[6] MA: O ven. Bakkula
considerou que durante toda a sua vida ele nunca se permitiu ser um fardo para
os outros bhikkhus, e ele não queria que o seu corpo fosse um fardo depois da
morte. Assim ele entrou numa meditação com o elemento fogo e realizou o parinibbana
fazendo com que todo o seu corpo fosse consumido pelas chamas.<span
style="mso-spacerun: yes">  </span>E só restaram as relíquias. [<a href="#R6">Retorna</a>]<o:p></o:p></span></p>


