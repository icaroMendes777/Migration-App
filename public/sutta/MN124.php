<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião o venerável Bakkula estava em Rajagaha, no Bambual, no
Santuário dos Esquilos.[<a href="#N1">1</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2. Então, Acela Kassapa, um antigo companheiro do venerável Bakkula na
sua vida laica, foi até o venerável Bakkula e ambos se cumprimentaram. Quando a
conversa cortês e amigável havia terminado, ele sentou a um lado e perguntou ao
venerável Bakkula:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. “Amigo
Bakkula, quanto tempo faz que você seguiu a vida santa?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“São
oitenta anos desde que segui a vida santa, amigo.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigo
Bakkula, nesses oitenta anos quantas vezes você teve relações sexuais?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigo
Kassapa, você não deveria fazer uma pergunta desse tipo. Você deveria
fazer uma pergunta deste tipo: ‘Amigo Bakkula, nesses oitenta anos quantas
vezes as percepções de desejo sexual surgiram em você?’” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigo
Bakkula, nesses oitenta anos quantas vezes as percepções de desejo sexual
surgiram em você?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigo
Kassapa, nos oitenta anos desde que segui a vida santa eu não me recordo de
nenhuma percepção de desejo sexual ter surgido em mim uma vez sequer.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>[Que nos
oitenta anos, desde que ele seguiu a vida santa, o venerável Bakkula não se
recorde de nenhuma percepção de desejo sexual ter surgido nele uma vez sequer
– disso nos lembraremos como uma qualidade maravilhosa e admirável do venerável
Bakkula.] [<a href="#N2">2</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>4-5. “Amigo Kassapa, nos oitenta anos desde que segui a vida santa eu
não me recordo de nenhuma percepção de má vontade ... de nenhuma percepção de
crueldade ter surgido em mim uma vez sequer.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>[Que nos
oitenta anos, desde que ele seguiu a vida santa, o venerável Bakkula não se
recorde de nenhuma percepção de má vontade ... nenhuma percepção de crueldade
ter surgido nele uma vez sequer – disso nos lembraremos como uma qualidade
maravilhosa e admirável do venerável Bakkula.]<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. “Amigo
Kassapa, nos oitenta anos desde que segui a vida santa eu não me recordo de
nenhum pensamento de desejo sexual ter surgido em mim uma vez sequer.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>[... disso
também nos lembraremos como uma qualidade maravilhosa e admirável do venerável
Bakkula.]<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7-8. “Amigo
Kassapa, nos oitenta anos desde que segui a vida santa eu não me recordo de
nenhum pensamento de má vontade ... de nenhum pensamento de crueldade ter
surgido em mim uma vez sequer.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>[...disso
também nos lembraremos como uma qualidade maravilhosa e admirável do venerável
Bakkula]<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9-15.
“Amigo Kassapa, nos oitenta anos desde que segui a vida santa eu não me recordo
de nenhuma vez ter aceito um manto dum chefe de família [<a href="#N3">3</a>]
... <a name=R3></a>de nenhuma vez ter usado um manto dado por um chefe de família
... de nenhuma vez ter cortado um manto com uma tesoura ... de nenhuma vez ter
costurado um manto com uma agulha ... de nenhuma vez ter tingido um manto com
corante ... de nenhuma vez ter confeccionado um manto no dia de kathina ...
de nenhuma vez ter trabalhado na confecção de mantos para os companheiros na
vida santa.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>[...disso
também nos lembraremos como uma qualidade maravilhosa e admirável do venerável
Bakkula.]<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16-19.
“Amigo, nos oitenta anos desde que segui a vida santa eu não me recordo de
nenhuma vez ter aceito um convite para uma refeição ... de nenhuma vez ter tido o
pensamento: ‘Oh, que alguém me convide para uma refeição!’ ... de nenhuma vez
ter-me sentado dentro de uma casa ... de nenhuma vez ter comido dentro de uma
casa.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>[... disso
também nos lembraremos como uma qualidade maravilhosa e admirável do venerável
Bakkula.]<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>20-25.
“Amigo, nos oitenta anos desde que segui a vida santa eu não me recordo de
nenhuma vez ter apreendido os sinais<span style="mso-spacerun: yes">  </span>ou
detalhes de uma mulher ... de nenhuma vez ter ensinado o Dhamma para uma mulher,
mesmo que fossem quatro versos duma estrofe ... de nenhuma vez ter ido até o
alojamento das bhikkhunis ... de nenhuma vez ter ensinado o Dhamma para uma
bhikkhuni ... de nenhuma vez ter ensinado o Dhamma para uma aprendiz ... de
nenhuma vez ter ensinado o Dhamma para uma noviça.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>[...disso
também nos lembraremos como uma qualidade maravilhosa e admirável do venerável
Bakkula.].<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>26-29.
“Amigo, nos oitenta anos desde que segui a vida santa eu não me recordo de
nenhuma vez ter dado a admissão na vida santa ... de nenhuma vez ter dado a
admissão completa ... de nenhuma vez ter dado dependência ... de nenhuma vez ter
tido um noviço me acompanhando.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>[...disso
também nos lembraremos como uma qualidade maravilhosa e admirável do venerável
Bakkula.]<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>30-37.
“Amigo, nos oitenta anos desde que segui a vida santa eu não me recordo de
nenhuma vez ter-me banhado numa casa de banhos ... de nenhuma vez ter-me banhado
com pó de banho .. de nenhuma vez ter desempenhado a tarefa de massagear os
membros dos meus companheiros na vida santa ... de nenhuma vez uma aflição ter
surgido em mim, ainda que pelo tempo que leva para ordenhar uma vaca ... de
nenhuma vez ter tomado medicamentos, ainda que um pedaço de uma noz ... de
nenhuma vez ter usado uma almofada ... de nenhuma vez ter arrumado uma cama ...
de nenhuma vez para o retiro das chuvas ter entrado em residência<span
style="mso-spacerun: yes">  </span>num alojamento dentro dum vilarejo.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>[...disso
também nos lembraremos como uma qualidade maravilhosa e admirável do venerável
Bakkula.]<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>38. “Amigo,
durante sete dias depois de ter seguido a vida santa eu me alimentei de comida
esmolada como um devedor; no oitavo dia o conhecimento supremo surgiu.”[<a
href="#N4">4</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[Que durante sete dias o venerável Bakkula tenha se alimentado de comida
esmolada como um devedor; e que no oitavo dia o conhecimento supremo tenha
surgido nele - disso também nos lembraremos como uma qualidade maravilhosa e
admirável do venerável Bakkula.]<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>39. [Então
Acela Kassapa disse:] “Eu receberia a admissão neste Dhamma e Disciplina, eu
receberia a admissão completa.” E Acela Kassapa recebeu a admissão neste Dhamma
e Disciplina, ele recebeu a admissão completa.[<a href="#N5">5</a>] </span><a
name=R5></a>Permanecendo só, isolado, diligente, ardente e decidido, em pouco
tempo, ele alcançou e permaneceu no objetivo supremo da vida santa pelo qual
membros de um clã deixam a vida em família pela vida santa, tendo conhecido e
realizado por si mesmo no aqui e agora. Ele soube: “O nascimento foi destruído,
a vida santa foi vivida, o que deveria ser feito foi feito, não há mais vir a
ser a nenhum estado.” <span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style="mso-spacerun: yes"> </span>E o venerável Kassapa se tornou um dos
Arahants.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>40. Então,
numa outra ocasião, o venerável Bakkula tomou uma chave e foi de cela em cela,
dizendo: “Venham, veneráveis senhores; venham, veneráveis senhores. Hoje
realizarei o parinibbana.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>[Que o
venerável Bakkula tenha tomado uma chave e tenha ido de cela em cela, dizendo:
“Venham, veneráveis senhores; venham, veneráveis senhores. Hoje realizarei o parinibbana” -disso também nos lembraremos como uma qualidade maravilhosa e
admirável do venerável Bakkula]<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>41. Então,
sentado no meio da Sangha de bhikkhus, o venerável Bakkula realizou o parinibbana. [<a href="#N6">6</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R6></a><a name=R7></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>[Que sentado no meio da Sangha de bhikkhus, o
venerável Bakkula tenha realizado o parinibbana - disso também nos
lembraremos como uma qualidade maravilhosa e admirável do venerável Bakkula.][<a
href="#N7">7</a>]<o:p></o:p></span></p>


