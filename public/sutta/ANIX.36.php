<p class=Normal> &quot;Eu lhes digo, o fim das impurezas da mente depende do primeiro
jhana ... do segundo jhana ... do terceiro jhana ... do quarto jhana ... da esfera do espaço infinito ... da
esfera da consciência infinita ... da esfera do nada. Eu lhes digo, o fim das
impurezas mentais depende da esfera da nem percepção, nem não percepção. <o:p></o:p></span></p>


<p class=Normal>&quot;'Eu lhes digo, o fim das impurezas mentais depende do primeiro
jhana.’ Assim foi dito. Em referência a que foi dito isto? É o caso em que um bhikkhu afastado dos prazeres sensuais, afastado das qualidades não hábeis, entra e permanece no primeiro jhana, que é caracterizado pelo pensamento aplicado e sustentado, com o êxtase e felicidade nascidos do afastamento. Ele observa qualquer fenômeno que ocorra<span style="mso-spacerun:
yes">  </span>conectado com o corpo, sensações, percepções, formações e
consciência, como impermanente, sofrimento, uma enfermidade, um câncer, uma
flecha, doloroso, uma aflição, estranho, uma dissolução, um vazio. Ele afasta a
sua mente desse fenômeno, e tendo feito isso, dirige a sua mente para as
características do imortal: ‘Isto é a paz, isto é o sublime, isto é, o
silenciar de todas as formações, o abandono de todas as aquisições, a
destruição do desejo; desapego; cessação; nibbana.’ <o:p></o:p></span></p>


<p class=Normal> &quot;Suponha que um arqueiro ou aprendiz de arqueiro fosse praticar em
um boneco de palha ou monte de barro, de forma que após algum tempo ele fosse
capaz de atirar a<span style="mso-spacerun: yes">  </span>longas distâncias,
atirar com precisão uma série rápida, e de penetrar uma grande massa. Da mesma
forma, é o caso em que um bhikkhu ... entra e permanece no primeiro jhana ...
êxtase e prazer nascidos do afastamento. Ele observa qualquer fenômeno que
ocorra<span style="mso-spacerun: yes">  </span>conectado com o corpo,
sensações, percepções, formações e consciência, como impermanente, sofrimento,
uma enfermidade, um câncer, uma flecha, doloroso, uma aflição, estranho, uma
dissolução, um vazio. Ele afasta a sua mente desse fenômeno, e tendo feito
isso, dirige a sua mente para as características do imortal: ‘Isto é a paz,
isto é o sublime, isto é, o silenciar de todas as formações, o abandono de
todas as aquisições, a destruição do desejo; desapego; cessação; nibbana.’ <o:p></o:p></span></p>


<p class=Normal> "Permanecendo exatamente ali, ele alcança o fim das impurezas mentais. Ou, 
se não, então - devido a esse desejo pelo Dhamma, esse deleite com o Dhamma 
e com a destruição dos cinco primeiros grilhões - ele irá renascer espontaneamente [nas Moradas Puras] e lá irá realizar o parinibbana sem nunca mais retornar desse mundo.<o:p></o:p></span></p>


<p class=Normal> &quot;'Eu lhes digo, o fim das impurezas mentais depende do primeiro
jhana.’ Assim foi dito, e em referência a isso é que foi dito. <o:p></o:p></span></p>

<p class=Normal> [Da mesma forma com os demais níveis de jhana até a esfera do nada.] <o:p></o:p></span></p>

<p class=Normal> &quot;Dessa forma, até o ponto em que vão as realizações da percepção,
até esse ponto chega o conhecimento. Quanto a essas duas esferas - a realização
da esfera de nem percepção, nem não percepção, e a realização da cessação da
sensação e percepção - eu lhes digo que estas devem ser explicadas corretamente
por aqueles bhikkhus que são meditadores, habilidosos em realizá-las,
habilidosos em realizá-las e emergir delas, que as realizaram e emergiram
delas.&quot; <o:p></o:p></span></p>

