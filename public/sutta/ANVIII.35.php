<p class=Normal> "Bhikkhus, há esses oito tipos de renascimento por conta da generosidade. Quais oito? </p>

<p class=Normal> (1) "Aqui, alguém dá uma oferenda para um contemplativo ou brâmane: comida, bebida, roupas e veículos; grinaldas, perfumes e ungüentos; cama, moradia e lamparinas. Qualquer coisa que ele dê, ele espera algo em retorno. Ele vê khattiyas afluentes, brâmanes afluentes, chefes de família afluentes providos e dotados com os cinco elementos do prazer sensual desfrutando deles com prazer. Ele pensa: 'Ah, com a dissolução do corpo, após a morte, que eu possa renascer na companhia de khattiyas afluentes, brâmanes afluentes, ou chefes de família afluentes!'  Ele marca a sua mente naquilo, ele fixa a sua mente naquilo, e desenvolve esse estado mental. Essa aspiração dele, resoluto naquilo que é inferior, sem desenvolver o superior, conduz ao renascimento ali. Com a dissolução do corpo, após a morte, ele renasce na companhia de khattiyas afluentes, brâmanes afluentes, ou chefes de família afluentes - e isso ocorre para quem é virtuoso, eu digo, não para quem é imoral. O desejo no coração de alguém que é virtuoso é satisfeito devido à sua pureza. </p>

<p class=Normal> (2) "Outra pessoa dá uma oferenda para um contemplativo ou brâmane: comida, bebida, ... e lamparinas. Qualquer coisa que ele dê, ele espera algo em retorno. Ele ouviu: 'Os devas governados pelos quatro grandes reis possuem vida longa, são belos e desfrutam de grande felicidade.' Ele pensa: 'Ah, com a dissolução do corpo, após a morte, que eu possa renascer na companhia dos devas governados pelos quatro grandes reis!'  Ele marca a sua mente naquilo, ele fixa a sua mente naquilo, e desenvolve esse estado mental. Essa aspiração dele, resoluto naquilo que é inferior, sem desenvolver o superior, conduz ao renascimento ali. Com a dissolução do corpo, após a morte, ele renasce na companhia dos devas governados pelos quatro grandes reis - e isso ocorre para quem é virtuoso, eu digo, não para quem é imoral. O desejo no coração de alguém que é virtuoso é satisfeito devido à sua pureza. </p>

<p class=Normal> (3) - (7) (2) "Outra pessoa dá uma oferenda para um contemplativo ou brâmane: comida, bebida, ... e lamparinas. Qualquer coisa que ele dê, ele espera algo em retorno. Ele ouviu: 'Os devas de Tavatimsa ... os devas de Yama ... os devas de Tusita ... os devas que se deliciam com a criação ... os devas que exercem poder sobre a criação de outros possuem vida longa, são belos e desfrutam de grande felicidade.' Ele pensa: 'Ah, com a dissolução do corpo, após a morte, que eu possa renascer na companhia dos devas que exercem poder sobre a criação de outros!' Ele marca a sua mente naquilo, ele fixa a sua mente naquilo, e desenvolve esse estado mental. Essa aspiração dele, resoluto naquilo que é inferior, sem desenvolver o superior, conduz ao renascimento ali. Com a dissolução do corpo, após a morte, ele renasce na companhia dos devas que exercem poder sobre a criação de outros - e isso ocorre para quem é virtuoso, eu digo, não para quem é imoral. O desejo no coração de alguém que é virtuoso é satisfeito devido à sua pureza. </p>

<p class=Normal> (8) "Outra pessoa dá uma oferenda para um contemplativo ou brâmane: comida, bebida, ... e lamparinas. Qualquer coisa que ele dê, ele espera algo em retorno. Ele ouviu: 'Os devas do cortejo de Brahma  possuem vida longa, são belos e desfrutam de grande felicidade.' Ele pensa: 'Ah, com a dissolução do corpo, após a morte, que eu possa renascer na companhia dos devas do cortejo de Brahma!'  Ele marca a sua mente naquilo, ele fixa a sua mente naquilo, e desenvolve esse estado mental. Essa aspiração dele, resoluto naquilo que é inferior, sem desenvolver o superior, conduz ao renascimento ali. Com a dissolução do corpo, após a morte, ele renasce na companhia dos devas do cortejo de Brahma  - e isso ocorre para quem é virtuoso, eu digo, não para quem é imoral; para alguém desprovido de cobiça, não para alguém cobiçoso. [<a href="#N1">1</a>] O desejo no coração de alguém que é virtuoso é satisfeito devido à sua pureza. </p>

<p class=Normal> "Esses bhikkhus são os oito tipos de renascimento por conta da generosidade."
</p>

