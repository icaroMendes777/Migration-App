<p class=Normal> (134) "Bhikkhus, possuindo duas qualidades, a pessoa tola, incompetente, ruim, se mantém numa condição mutilada e prejudicada; ela é passível de crítica e sujeita à censura pelos sábios; e ela gera muito demérito. Quais duas? Sem investigar e examinar, ela elogia quem merece censura. Sem investigar e examinar, ela censura quem merece elogio. Possuindo essas duas qualidades, a pessoa tola, incompetente, ruim, se mantém numa condição mutilada e prejudicada; ela é passível de crítica e sujeita à censura pelos sábios; e ela gera muito demérito. </p>

<p class=Normal>"Bhikkhus, possuindo duas qualidades, a pessoa sábia, competente, boa, se mantém numa condição íntegra e sem prejuízo; ela não é passível de crítica e não está sujeita à censura pelos sábios; e ela gera muito mérito. Quais duas? Tendo investigado e examinado, ela elogia quem merece elogio. Tendo investigado e examinado, ela censura quem merece censura. Possuindo essas duas qualidades, a pessoa sábia, competente, boa, se mantém numa condição íntegra e sem prejuízo; ela não é passível de crítica e não está sujeita à censura pelos sábios; e ela gera muito mérito." </p>

<p class=Normal> (135) "Bhikkhus, possuindo duas qualidades, a pessoa tola, incompetente, ruim, se mantém numa condição mutilada e prejudicada; ela é passível de crítica e sujeita à censura pelos sábios; e ela gera muito demérito. Quais duas? Sem investigar e examinar, ela suspeita algo que merece crédito. Sem investigar e examinar, ela dá crédito a algo que é suspeito. Possuindo essas duas qualidades, a pessoa tola, incompetente, ruim se mantém numa condição mutilada e prejudicada; ela é passível de crítica e sujeita à censura pelos sábios; e ela gera muito demérito. </p>

<p class=Normal>"Bhikkhus, possuindo duas qualidades, a pessoa sábia, competente, boa, se mantém numa condição íntegra e sem prejuízo; ela não é passível de crítica e não está sujeita à censura pelos sábios; e ela gera muito mérito. Quais duas? Tendo investigado e examinado, suspeita algo que é suspeito. Tendo investigado e examinado, ela  dá crédito a algo que merece crédito. Possuindo essas duas qualidades, a pessoa sábia, competente, boa, se mantém numa condição íntegra e sem prejuízo; ela não é passível de crítica e não está sujeita à censura pelos sábios; e ela gera muito mérito." </p>

<p class=Normal> (136) "Bhikkhus, comportando-se de forma incorreta em relação a duas pessoas, a pessoa tola, incompetente, ruim, se mantém numa condição mutilada e prejudicada; ela é passível de crítica e sujeita à censura pelos sábios; e ela gera muito demérito. Quais duas? A própria mãe e o próprio pai. Comportando-se de forma incorreta em relação a duas pessoas, a pessoa tola, incompetente, ruim, se mantém numa condição mutilada e prejudicada; ela é passível de crítica e sujeita à censura pelos sábios; e ela gera muito demérito. </p>

<p class=Normal>"Bhikkhus, comportando-se de forma correta em relação a duas pessoas, a pessoa sábia, competente, boa, se mantém numa condição íntegra e sem prejuízo; ela não é passível de crítica e não está sujeita à censura pelos sábios; e ela gera muito mérito. Quais duas? A própria mãe e o próprio pai. Comportando-se de forma correta em relação a duas pessoas, a pessoa sábia, competente, boa, se mantém numa condição íntegra e sem prejuízo; ela não é passível de crítica e não está sujeita à censura pelos sábios; e ela gera muito mérito." </p>


