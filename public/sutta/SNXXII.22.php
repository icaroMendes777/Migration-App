<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, eu ensinarei para vocês sobre o fardo, sobre aquele que carrega
o fardo, sobre o levantamento do fardo e a sobre a deposição do fardo. Ouçam e
prestem muita atenção àquilo que eu vou dizer.” – “Sim, venerável senhor,” os
bhikkhus responderam. O Abençoado disse o seguinte:<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style="mso-spacerun: yes"> </span>“E qual é o fardo? ‘Os cinco agregados
influenciados pelo apego,’ deveria ser dito. Quais cinco? A forma como um
agregado influenciado pelo apego, a sensação como um agregado influenciado pelo
apego, a percepção como um agregado influenciado pelo apego, as formações volitivas como
um agregado influenciado pelo apego, a consciência como um agregado
influenciado pelo apego. A isto, bhikkhus se denomina o fardo.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E quem é
aquele que carrega o fardo? ‘A pessoa,’ deveria ser dito. Este venerável com
tal e qual nome. Ele é chamado o carregador do fardo.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que é
o levantamento do fardo? O desejo que causa a renovação dos seres, acompanhado
pelo prazer e pela cobiça e que sente prazer nisto e naquilo; isto é o desejo
pelos prazeres sensuais, desejo por ser/existir e desejo por não ser/existir. A
isto se denomina o levantamento do fardo.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que é
a deposição do fardo? O desaparecimento e cessação sem deixar vestígios, abrir mão, descartar, libertar-se, despegar
 daquele mesmo desejo. A isto se denomina a
deposição do fardo.”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o
que o Abençoado disse. Tendo dito isso, o Mestre disse mais:<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>
Um fardo de fato são os cinco
agregados, <br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-tab-count:1'>            </span>e o carregador do fardo é a pessoa.<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-tab-count:1'>            </span>Levantar o fardo é o sofrimento no
mundo.<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-tab-count:1'>            </span>Depor o fardo é a felicidade.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>
Tendo deposto o pesado fardo<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-tab-count:1'>            </span>e não tomando outro,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-tab-count:1'>            </span>arrancando o desejo,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-tab-count:1'>            </span>junto com a sua raiz,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-tab-count:1'>            </span>ele está livre da fome, totalmente
saciado.<o:p></o:p></span></p>


