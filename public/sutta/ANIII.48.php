<p class=Normal> "Bhikkhus, baseado na neve do Himalaya, o rei das montanhas, notáveis árvores sal crescem de três modos. Quais três? (1) Os seus galhos, folhas e flores crescem; (2) a sua casca e brotos crescem; (3) e o seu cerne e alburno crescem. Baseado na neve do Himalaya, o rei das montanhas, notáveis árvores sal crescem desses três modos.</p>

<p class=Normal>"Da mesma forma, quando o cabeça da família está dotado de convicção, as pessoas na família que dele dependem crescem de três modos. Quais três? (1) Elas crescem em convicção; (2) elas crescem no comportamento virtuoso; (3) e elas crescem em sabedoria. Quando o cabeça da família está dotado de convicção, as pessoas na família que dele dependem crescem desses três modos."</p>

<p class=Normal>Tal como as árvores que crescem <br>
numa vasta floresta virgem, <br>
na dependência de uma montanha rochosa, <br>
podem se transformar em notáveis "senhores das florestas," <br>
assim também, quando o cabeça da família<br>
está dotado com convicção e virtude, <br>
a sua esposa, filhos e parentes, <br>
todos crescem na dependência dele; <br>
do mesmo modo os seus amigos, o seu círculo familiar, <br>
e aqueles que dele dependem. </p>

<p class=Normal>Aqueles que possuem discernimento, <br>
vendo a conduta desse homem virtuoso, <br>
a sua generosidade e boas ações, <br>
seguem o seu exemplo. </p>

<p class=Normal>Tendo aqui vivido de acordo com o Dhamma, <br>
o caminho que conduz a um bom destino, <br>
aqueles que desejam os prazeres dos sentidos se alegram, <br>
deliciando-se no mundo dos devas.</p>

