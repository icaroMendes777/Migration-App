<p class=Normal> Em certa ocasião o Abençoado estava em Campa, às margens do Lago da Lótus Gaggara. Então o chefe de família Vajjiyamahita deixou Campa cedo pela manhã para ver o Abençoado, porém o pensamento lhe ocorreu, “Não é o momento apropriado para ver o Abençoado, ele ainda está em retiro. E não é o momento apropriado para ver os bhikkhus dignos de respeito, eles ainda estão em retiro. E se eu fosse até o parque dos errantes de outras seitas?" E ele foi até o parque onde estavam os errantes de outras seitas. </p>

<p class=Normal> Agora, naquela ocasião os errantes de outras seitas estavam sentados numa grande assembléia fazendo uma grande baderna, conversando em voz alta e aos berros sobre muitos assuntos inúteis. Eles viram o chefe de família Vajjiyamahita vindo à distância. Ao vê-lo, eles silenciaram a assembléia dizendo o seguinte: “Senhores, fiquem quietos; senhores, não façam ruído. Ali vem o chefe de família Vajjiyamahita, um discípulo do contemplativo Gotama, um dos discípulos leigos vestidos de branco do contemplativo Gotama que está em Campa. Esses veneráveis gostam do silêncio; eles são disciplinados no silêncio; eles recomendam o silêncio. Talvez, se ele encontrar a nossa assembléia em silêncio, ele pensará em juntar-se a nós.” Então os errantes ficaram em silêncio. </p>

<p class=Normal> Então o chefe de família Vajjiyamahita foi até onde os errantes de outras seitas estavam e eles se cumprimentaram. Quando a conversa amigável e cortês havia terminado, ele sentou a um lado e os errantes lhe disseram: </p>

<p class=Normal> "Chefe de família, é verdade o que dizem, que o contemplativo Gotama critica todas as austeridades e que ele condena e reprova incondicionalmente todos aqueles que vivem uma vida austera e dura?" </p>

<p class=Normal> "Não, venerável senhor, o Abençoado não critica todas as austeridades e ele não condena e reprova incondicionalmente todos aqueles que vivem uma vida austera e dura. O Abençoado critica o que merece ser criticado e elogia o que merece ser elogiado. Criticando aquilo que merece ser criticado e elogiando o que merece ser elogiado o Abençoado fala depois de fazer uma análise; ele não fala de um modo unilateral." [<a href="#N1">1</a>] </p>

<p class=Normal><a name=R1></a> Quando isso foi dito um dos errantes falou: "Espere um pouco, chefe de família! Esse contemplativo Gotama que você está elogiando é aquele que se recusa a fazer declarações definitivas." </p>

<p class=Normal> "Também responderei a isso, venerável senhor. O Abençoado declarou: 'Isto é benéfico' e, 'Isto é prejudicial.' Portanto quando o Abençoado declara o que é benéfico e o que é prejudicial, ele faz declarações definitivas. Ele não se recusa a fazer declarações definitivas." </p>

<p class=Normal> Quando isso foi dito, os errantes ficaram sentados em silêncio, consternados, com os ombros caídos e a cabeça baixa, deprimidos e sem resposta. Vajjiyamahita percebendo que os errantes estavam sentados em silêncio, consternados ... sem resposta, foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e relatou ao Abençoado toda a conversa com os errantes. </p>

<p class=Normal> [O Abençoado disse:] Muito bem, chefe de família. Muito bem. Assim é como você deve sempre refutar com o Dhamma esses homens tolos.” </p>

<p class=Normal> (1) - (2) "Eu não digo, chefe de família, que todos os tipos de austeridades devam ser praticadas; nem digo que todos os tipos de austeridades não devam ser praticadas. (3) - (4) Eu não digo que todos os tipos de observâncias devam ser praticadas; nem digo que todos os tipos de observâncias não devam ser praticadas. (5) - (6) Eu não digo que alguém deve se esforçar de todos os modos; nem digo que alguém não deve se esforçar de nenhum modo. (7) - (8) Eu não digo que alguém deva fazer todo tipo de renúncia; nem digo que alguém não deva fazer nenhum tipo de renúncia. (9) - (10) Eu não digo que alguém deva realizar todo tipo de libertação; nem digo que alguém não deva realizar nenhum tipo de libertação. </p>

<p class=Normal> (1) - (2) "Se, chefe de família, quando alguém pratica uma certa austeridade, as qualidades prejudiciais aumentam e as qualidades benéficas diminuem, então eu digo, ele não deve praticar essa austeridade. Mas se quando alguém pratica uma certa austeridade, as qualidades prejudiciais diminuem e as qualidades benéficas aumentam, então eu digo, ele deve praticar essa austeridade. </p>

<p class=Normal> (3) - (4) "Se, chefe de família, quando alguém pratica uma certa observância, as qualidades prejudiciais aumentam e as qualidades benéficas diminuem, então eu digo, ele não deve praticar essa observância. Mas se quando alguém pratica uma certa observância, as qualidades prejudiciais diminuem e as qualidades benéficas aumentam, então eu digo, ele deve praticar essa observância. </p>

<p class=Normal> (5) - (6) "Se, chefe de família, quando alguém se esforça de um certo modo, as qualidades prejudiciais aumentam e as qualidades benéficas diminuem, então eu digo, ele não deve se esforçar desse modo. Mas se quando alguém se esforça de um certo modo, as qualidades prejudiciais diminuem e as qualidades benéficas aumentam, então eu digo, ele deve se esforçar desse modo. </p>

<p class=Normal> (7) - (8) "Se, chefe de família, quando alguém ao renunciar algo, as qualidades prejudiciais aumentam e as qualidades benéficas diminuem, então eu digo, ele não deve fazer essa renúncia. Mas se quando alguém ao renunciar algo, as qualidades prejudiciais diminuem e as qualidades benéficas aumentam, então eu digo, ele deve fazer essa renúncia. </p>

<p class=Normal> (9) - (10) "Se, chefe de família, quando alguém realiza uma certa libertação, as qualidades prejudiciais aumentam e as qualidades benéficas diminuem, então eu digo, ele não deve realizar essa libertação. Mas se quando alguém realiza uma certa libertação, as qualidades prejudiciais diminuem e as qualidades benéficas aumentam, então eu digo, ele deve realizar essa libertação. </p>

<p class=Normal> Quando Vajjiyamahita havia sido instruído, estimulado, motivado e encorajado pelo Abençoado com um discurso do Dhamma, ele levantou do seu assento e depois de homenagear o Abençoado, mantendo-o à sua direita, partiu. </p>

<p class=Normal> Não muito tempo depois o Abençoado dirigiu-se aos bhikkhus: “Bhikkhus, mesmo um bhikkhu que há muito tenha compreendido o Dhamma nesta Doutrina e Disciplina faria bem em sempre refutar os errantes de outras seitas com o Dhamma exatamente da forma como fez o chefe de família Vajjiyamahita."
</p>


