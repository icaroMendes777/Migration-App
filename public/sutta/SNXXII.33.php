<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, tudo aquilo que não é seu, abandonem-no. Ao abandoná-lo, isso
irá conduzir ao seu bem-estar e felicidade. [<a href="#N1">1</a>] <a name=R1></a>E
o que, bhikkhus, não é seu? A forma material não é sua, abandonem-na. Ao
abandoná-la, isso irá conduzir ao seu bem-estar e felicidade. A sensação não é
sua, abandonem-na. Ao abandoná-la, isso irá conduzir ao seu bem-estar e felicidade.
A percepção não é sua, abandonem-na. Ao abandoná-la, isso irá conduzir ao seu
bem-estar e felicidade. As formações volitivas não são suas, abandonem-nas. Ao
abandoná-las, isso irá conduzir ao seu bem-estar e felicidade. A consciência
não é sua, abandonem-na. Ao abandoná-la, isso irá conduzir ao seu bem-estar e
felicidade.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
o que vocês pensam? Se as pessoas levassem embora a grama, gravetos, galhos e
folhas deste bosque de Jeta, ou se os queimassem, ou fizessem com eles o que
desejassem, vocês pensariam: ‘As pessoas estão nos levando ou estão nos
queimando ou estão fazendo conosco o que desejam?’ – “Não, venerável senhor.
Porque não? Porque isso não é nem nosso eu, nem pertence ao nosso eu.” – “Da
mesma forma, bhikkhus, tudo aquilo que não é seu, <a name=PVW>abandonem-no</a>.
Ao abandoná-lo, isso irá conduzir ao seu bem-estar e felicidade. E o que,
bhikkhus, não é seu? A forma material não é sua ... A sensação não é sua ... A
percepção não é sua ... As formações volitivas não são suas ... A consciência não é sua,
abandonem-na. Ao abandoná-la, isso irá conduzir ao seu bem-estar e felicidade.<o:p></o:p></span></p>



