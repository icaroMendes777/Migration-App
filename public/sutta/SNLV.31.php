<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, há essas quatro fontes de méritos, fontes daquilo que é benéfico,
alimento da felicidade. Quais quatro?<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
bhikkhus, o nobre discípulo possui perfeita claridade, serenidade e confiança no Buda assim: ‘O </span>Abençoado
é um arahant, perfeitamente iluminado, consumado no verdadeiro conhecimento
e conduta, bem-aventurado, conhecedor dos mundos, um líder insuperável de
pessoas preparadas para serem treinadas, mestre de devas e humanos, desperto,
sublime<span lang=PT-BR style='mso-ansi-language:PT-BR'>.’ Essa é a primeira fonte
de mérito, fonte daquilo que é benéfico, alimento da felicidade.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Outra vez,
bhikkhus, o nobre discípulo possui perfeita claridade, serenidade e confiança no Dhamma assim: ‘</span>O
Dhamma é bem proclamado pelo Abençoado, visível no aqui e agora, com efeito
imediato, que convida ao exame, que conduz para adiante, para ser experimentado
pelos sábios por eles mesmos.’<span style='mso-ansi-language:PT-BR'> <span
lang=PT-BR>Essa é a segunda fonte de mérito, fonte daquilo que é benéfico, alimento
da felicidade.<o:p></o:p></span></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Outra vez,
bhikkhus, o nobre discípulo possui perfeita claridade, serenidade e confiança na Sangha assim: ‘</span>Sangha
dos discípulos do Abençoado pratica o bom caminho, pratica o caminho reto,
pratica o caminho verdadeiro, pratica o caminho adequado, isto é, os quatro
pares de pessoas, os oito tipos de indivíduos; esta Sangha dos discípulos do
Abençoado é merecedora de dádivas, merecedora de hospitalidade, merecedora de
oferendas, merecedora de saudações com reverência, um campo inigualável de
mérito para o mundo<span lang=PT-BR style='mso-ansi-language:PT-BR'>.’ Essa é a
terceira fonte de mérito, fonte daquilo que é benéfico, alimento da felicidade.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Outra vez,
bhikkhus, o nobre discípulo possui as virtudes apreciadas pelos nobres –
intactas, não-laceradas, imaculadas, não-matizadas, libertadoras, elogiadas
pelos sábios, desapegadas, que conduzem à concentração. Essa é a quarta fonte de
mérito, fonte daquilo que é benéfico, alimento da felicidade.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Essas são as
quatro fontes de méritos, fontes daquilo que é benéfico, alimento da felicidade.”<o:p></o:p></span></p>


