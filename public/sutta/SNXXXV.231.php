<p class=Normal> "Bhikkhus, com relação às formas conscientizadas através do olho, se em algum bhikkhu ou bhikkhuni ainda existe cobiça que não tenha sido abandonada, se ainda existe raiva que não tenha sido abandonada, se ainda existe delusão que não tenha sido abandonada, então mesmo formas insignificantes que entrem no campo de visão obcecam a mente, o que dizer então daquelas formas que são proeminentes. Por qual razão? Porque a cobiça ainda está presente e não foi abandonada, a raiva ainda está presente e não foi abandonada, a delusão ainda está presente e não foi abandonada. O mesmo em relação aos sons conscientizados através do ouvido ... objetos mentais conscientizados através da mente. </p>

<p class=Normal> "Suponham, bhikkhus, que houvesse uma árvore leiteira - jovem, suculenta. Se um homem a cortasse aqui e ali com um machado, o leite da árvore escorreria?" </p>

<p class=Normal> "Sim, venerável senhor. Por qual razão? Porque há leite na árvore." </p>

<p class=Normal> "Do mesmo modo, bhikkhus, com relação a formas conscientizadas através do olho ... mesmo formas insignificantes que entrem no campo de visão obcecam a mente, o que dizer então daquelas formas que são proeminentes. Por qual razão? Porque a cobiça ainda está presente e não foi abandonada, a raiva ainda está presente e não foi abandonada, a delusão ainda está presente e não foi abandonada. O mesmo em relação aos sons conscientizados através do ouvido ... objetos mentais conscientizados através da mente. </p>

<p class=Normal> "Bhikkhus, com relação às formas conscientizadas através do olho, se em algum bhikkhu ou bhikkhuni não existe cobiça e esta tenha sido abandonada, se não existe raiva e esta tenha sido abandonada, se não existe delusão e esta tenha sido abandonada, então mesmo formas proeminentes que entrem no campo de visão não obcecam a mente, o que dizer então daquelas formas que são insignificantes. Por qual razão? Porque a cobiça não está presente e foi abandonada, a raiva não está presente e foi abandonada, a delusão não está presente e foi abandonada. O mesmo em relação aos sons conscientizados através do ouvido ... objetos mentais conscientizados através da mente. </p>

