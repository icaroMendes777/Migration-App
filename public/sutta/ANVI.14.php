<p class=Normal> Lá o venerável Sariputta se dirigiu aos monges desta forma: "Amigos." – "Amigo," eles responderam. O venerável Sariputta disse o seguinte: </p>

<p class=Normal><a name=R1></a> "Amigos, um bhikkhu passa o tempo de tal modo que ele não terá uma morte boa. [<a href="#N1">1</a>] E como um bhikkhu passa o tempo de tal modo que ele não terá uma morte boa? </p>

<p class=Normal> "Aqui, (1) um bhikkhu se delicia com o trabalho, ele é dedicado ao trabalho; (2) ele se delicia com as conversas, ele é dedicado às conversas; (3) ele se delicia com o sono, ele é dedicado ao sono; (4) ele se delicia com as companhias, ele é dedicado às companhias; (5) ele delicia em se associar, ele é dedicado a se associar; (6) ele se delicia com a proliferação, ele é dedicado à proliferação. [<a href="#N2">2</a>]  <a name=R2></a>Quando um bhikkhu passa o tempo desse modo ele não terá uma morte boa. <a name=R3></a>Ele é chamado um bhikkhu que se delicia com a identidade, [<a href="#N3">3</a>]   que não abandonou a identidade para dar um completo fim ao sofrimento. </p>


<p class=Normal> "Amigos, um bhikkhu passa o tempo de tal modo que ele terá uma morte boa. E como um bhikkhu passa o tempo de tal modo que ele terá uma morte boa? </p>

<p class=Normal> "Aqui, (1) um bhikkhu não se delicia com o trabalho, ele não é dedicado ao trabalho; (2) ele não se delicia com as conversas, ele não é dedicado às conversas; (3) ele não se delicia com o sono, ele não é dedicado ao sono; (4) ele não se delicia com as companhias, ele não é dedicado às companhias; (5) ele não delicia em se associar, ele não é dedicado a se associar; (6) ele não se delicia com a proliferação, ele não é dedicado à proliferação. Quando um bhikkhu passa o tempo desse modo ele terá uma morte boa. Ele é chamado um bhikkhu que se delicia com nibbana,  que abandonou a identidade para dar um completo fim ao sofrimento. </p>

<p class=Normal><a name=R4></a> A criatura [<a href="#N4">4</a>] dedicada à proliferação, <br>
que se delicia com a proliferação, <br>
fracassou em realizar nibbana, <br>
a insuperável segurança contra o cativeiro. </p>

<p class=Normal> Mas aquele que abandonou a proliferação. <br>
que se delicia com a não-proliferação, <br>
realizou nibbana, <br>
a insuperável segurança contra o cativeiro.</p>


