PT-BR'>[9] <i style='mso-bidi-font-style:normal'>Janami passami tatheva etam</i>:
De acordo com BN essa frase aparece no Cânone como a característica dogmática
das idéias especulativas. Esta frase está em pé de igualdade com outra
afirmação dogmática: ‘somente isso é verdadeiro, todo o restante é falso’. [<a
