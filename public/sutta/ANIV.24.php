<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] <i style='mso-bidi-font-style:normal'>Muta</i>: sensações que surgem
do paladar, olfato e tangíveis. [<a href="#R1">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[2] De acordo com AA: o Tathagata não se apóia nisso ou aborda isso com
base no desejo ou idéias. O Abençoado vê uma forma através do olho mas nele não
surge o desejo ou cobiça por aquilo, a sua mente está bem libertada.Veja também
o <a href="SNXXXV.232.php">SN XXXV.232</a>. [<a href="#R2">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[3] <i style='mso-bidi-font-style:normal'>Na Maññati</i>: <i
style='mso-bidi-font-style:normal'>Maññati, </i>conceber, é o estágio no qual a
cognição atribui ao objeto externo a característica de realidade objetiva
resultando na dicotomia sujeito-objeto e perpetuando a presunção ‘eu’ e ‘meu’.
Para analisar mais a fundo o processo de geração das concepções e das
proliferações mentais, veja o <a href="MN18.php">MN 18</a> e as referências
adicionais na nota 7 desse sutta. [<a href="#R3">Retorna</a>] <o:p></o:p></span></p>



<p class=Normal><a name=N4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[4] <i style='mso-bidi-font-style:normal'>Adittham na maññati</i>: AA
diz que isto significa que o Tathagata não imagina (com base no desejo, etc.)
que ele está vendo algo que não foi visto por outras pessoas. O Bhikkhu
Ñanananda (BN) no seu livro <i style='mso-bidi-font-style:normal'>The Magic of
the Mind – An Exposition of the Kalakarama Sutta</i> dá uma explicação
distinta. Para ele essa expressão quer dizer exatamente o contrário, ou seja,
ela dá sustentação à afirmação “Se eu dissesse, ‘Tudo aquilo que no mundo ...
é<span style='mso-spacerun:yes'>  </span>visto, ouvido ... ponderado pela
mente: tudo isso eu não entendo,’ isso seria confusão da minha parte.” [<a
href="#R4">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N5></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[5] <i style='mso-bidi-font-style:normal'>Datthabbam na maññati</i>: BN
diz que o Tathagata não considera nenhuma das ‘visões’ que as pessoas valorizam
como ‘dignas de serem vistas’ – no seu sentido mais elevado. Ele não vê nada de
substancial nelas. [<a href="#R5">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[6] <i style='mso-bidi-font-style:normal'>Dattharam na maññati</i>: BN
diz que o Tathagata não possui nenhum tipo de presunção quanto a ser o agente
por detrás daquilo que é visto. Quando os objetos vistos perdem o seu status de
realidade objetiva eles não refletem um ‘agente’ no lado subjetivo. [<a
href="#R6">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N7></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[7] <i style='mso-bidi-font-style:normal'>Tatha</i> pode ser
interpretado como “assim, tal como, dessa forma.” As coisas como elas na
verdade são, ao invés do que aparentam ser. [<a href="#R7">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N8></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[8] De acordo com AA esse ‘gancho’ são as idéias dogmáticas que são
colocadas como verdades absolutas. O Tathagata viu esse ‘gancho’ antes, na
noite da sua iluminação. Veja também o <a href="SnpIV.8.php">Snp IV.8</a>. [<a
href="#R8">Retorna</a>]<o:p></o:p></span></p>



