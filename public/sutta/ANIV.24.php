<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em certa
ocasião o Abençoado estava<span style='mso-spacerun:yes'>  </span>em Saketa no
parque de Kalaka. Lá ele se dirigiu aos monges desta forma: “Bhikkhus” –
“Venerável Senhor,” eles responderam. O Abençoado disse o seguinte: <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
tudo aquilo que no mundo, incluindo os seus devas, maras e brahmas, esta
população com seus contemplativos e brâmanes, seus príncipes e povo é<span
style='mso-spacerun:yes'>  </span>visto, ouvido, sentido, [<a href="#N1">1</a>]
<a name=RA></a><a name=R1></a>conscientizado, buscado, procurado, ponderado
pela mente: tudo isso eu entendo. Tudo aquilo que no mundo, incluindo os seus
devas, maras e brahmas, esta população com seus contemplativos e brâmanes, seus
príncipes e povo é visto, ouvido, sentido, conscientizado, buscado,
procurado, ponderado pela mente: tudo isso eu compreendi completamente; tudo
isso é do conhecimento do Tathagata, mas o Tathagata não se estabelece nisso. [<a
href="#N2">2</a>] <o:p></o:p></span></p>



<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Se eu dissesse, ‘Tudo aquilo que no mundo ... é<span
style='mso-spacerun:yes'>  </span>visto, ouvido, sentido, conscientizado, buscado,
procurado, ponderado pela mente: tudo isso eu não entendo,’ isso seria confusão
da minha parte. Se eu dissesse, ‘Tudo aquilo que no mundo ... é visto, ouvido,
sentido, conscientizado, buscado, procurado, ponderado pela mente: tudo isso eu
ambos entendo e não entendo,’ isso também seria confusão da minha parte. Se eu
dissesse, ‘Tudo aquilo que no mundo ... é visto, ouvido, sentido,
conscientizado, buscado, procurado, ponderado pela mente: tudo isso eu nem
entendo tampouco não entendo,’ isso seria uma falta da minha parte.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
bhikkhus, ao ver aquilo que é para ser visto, o Tathagata não concebe [<a
href="#N3">3</a>] <a name=R3></a>algo visível separado do objeto visto. Ele não
concebe o não visto.[<a href="#N4">4</a>] <a name=R4></a>Ele não concebe um
objeto digno de ser visto. [<a href="#N5">5</a>] <a name=R5></a>Ele não concebe
aquele que vê.[<a href="#N6">6</a>] <o:p></o:p></span></p>



<p class=Normal> <a name=R6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Ao ouvir … <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ao sentir
…<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ao
conscientizar aquilo que é para ser conscientizado, o Tathagata não concebe algo
conscientizável separado do objeto conscientizado. Ele não concebe o não
conscientizado. Ele não concebe um objeto digno de se conscientizado. Ele não
concebe aquele que conscientiza.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
bhikkhus, o Tathagata, sendo o mesmo com relação a todos os fenômenos que podem
ser vistos, ouvidos, sentidos e conscientizados, é ‘Assim.’[<a href="#N7">7</a>]
<a name=R7></a>E eu lhes digo: Não há nenhum outro ‘Assim’ mais elevado ou mais
sublime. <o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Tudo aquilo que é visto, ouvido, sentido ou apegado,<br>

&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; é estimado como
verdadeiro pelos outros,<br>

entre aqueles entrincheirados nas suas próprias idéias, <br>

&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; sendo ‘Assim’ eu não tomo nenhuma como
verdadeira ou falsa.<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Tendo visto esse gancho muito antes, [<a href="#N8">8</a>]<br>

&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; <a name=R8></a>no qual gerações estão fisgadas,
empaladas.<br>

‘Eu entendo, eu vejo, de fato é
assim!’ [<a href="#N9">9</a>]<br>

&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; <a name=R9></a>Não há nada que sujeite os
Tathagatas.”<o:p></o:p></span></p>



