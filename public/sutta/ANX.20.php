<p class=Normal style='margin-left:.75in'> 

<p class=Normal>Certa ocasião, estava o Abençoado entre os Kurus numa cidade denominada Kammasadhamma. Lá ele se dirigiu aos monges desta forma: "Bhikkhus." – "Venerável Senhor," eles responderam. O Abençoado disse o seguinte: </p>

<p class=Normal> “Bhikkhus, há essas dez nobres permanências nas quais os nobres permaneceram [no passado], permanecem [no presente] e permanecerão [no futuro]. Quais dez? É o caso em que um bhikkhu abandonou cinco fatores, possui seis fatores, possui uma proteção, é suportado por quatro fatores, se livrou das verdades faccionárias, abandonou completamente a busca, clarificou os seus pensamentos, acalmou a formação corporal, com a mente bem libertada e bem libertado através da sabedoria. Essas são as dez nobres permanências nas quais os nobres permaneceram, permanecem e permanecerão. </p>

<p class=Normal> “E como um bhikkhu  abandonou cinco fatores? É o caso em que um bhikkhu abandonou o desejo pelo prazer dos sentidos, a má vontade, a preguiça e o torpor, a inquietação e a ansiedade, a dúvida. Assim ele abandonou cinco fatores. </p>

<p class=Normal> “E como um bhikkhu possui seis fatores? É o caso em que um bhikkhu, ao ver uma forma com o olho ele não se alegra, nem se entristece, mas permanece equânime, com atenção plena e plena consciência. Ao ouvir um som com o ouvido ... Ao cheirar um aroma com o nariz ... Ao saborear um sabor com a língua  ... Ao tocar um tangível com o corpo ... Ao conscientizar um objeto mental com a mente, ele não se alegra, nem se entristece, mas permanece equânime, com atenção plena e plena consciência. Assim ele possui seis fatores. </p>

<p class=Normal> “E como um bhikkhu possui uma proteção? É o caso em que um bhikkhu possui uma mente protegida pela atenção plena. Assim ele possui uma proteção. </p>

<p class=Normal> “E como um bhikkhu é suportado por quatro fatores? É o caso em que um bhikkhu, refletindo cuidadosamente, usa algumas coisas, tolera algumas coisas, evita algumas coisas e remove algumas coisas. Assim ele é suportado por quatro fatores. [<a href="#N1">1</a>] </p>

<p class=Normal><a name="R1"> </a> “E como um bhikkhu se livrou das verdades faccionárias? É o caso em que um bhikkhu se livrou das verdades faccionárias  dos brâmanes e contemplativos comuns, em outras palavras, ‘O mundo é finito,’ ‘O mundo é infinito,’ ‘A alma e o corpo são a mesma coisa,’ ‘A alma é uma coisa e o corpo outra,’ ‘Após a morte um Tathagata existe,’ ‘Após a morte um Tathagata não existe,’ ‘Após a morte um Tathagata tanto existe como não existe,’ ‘Após a morte um Tathagata nem existe nem não existe.’ Tudo isso ele descartou, se livrou, renunciou, rejeitou, abandonou, se soltou, abdicou. Assim ele se livrou das verdades faccionárias. </p> 

<p class=Normal> “E como um bhikkhu abandonou completamente a busca? É o caso em que um bhikkhu abandonou a busca pelo prazer dos sentidos, a busca por ser/existir e a busca por uma vida santa. [<a href="#N2">2</a>] <a name="R2"> </a>Assim ele abandonou completamente a busca. </p>

<p class=Normal> “E como um bhikkhu clarificou os seus pensamentos? É o caso em que um bhikkhu abandonou os pensamentos dos prazeres dos sentidos, os pensamentos de má vontade, os pensamentos de crueldade. Assim ele clarificou os seus pensamentos. </p>

<p class=Normal> “E como um bhikkhu acalmou a formação corporal? [<a href="#N3">3</a>] <a name="R3"> </a>É o caso em que um bhikkhu, com o completo desaparecimento da felicidade entra e permanece no
quarto jhana, que possui nem felicidade nem sofrimento, com a atenção plena
e a equanimidade purificadas. Assim ele acalmou a formação corporal. </p>

<p class=Normal> “E como um bhikkhu está com a mente bem libertada? É o caso em que a mente de um bhikkhu está libertada da cobiça, libertada da raiva, libertada da delusão. Assim ele  está com a mente bem libertada. </p>

<p class=Normal> “E como um bhikkhu está bem libertado através da sabedoria? É o caso em que um bhikkhu compreende, ‘A cobiça, raiva e delusão foram abandonadas por mim, cortadas pela raiz, feitas como um tronco de palmeira, eliminadas de tal forma que não estarão mais sujeitas a um futuro surgimento Assim ele está bem libertado através da sabedoria. </p>

<p class=Normal> “Bhikkhus, todos aqueles que no passado permaneceram em nobres permanências, permaneceram nessas mesmas dez nobres permanências. Todos aqueles que no futuro permanecerão em nobres permanências, permanecerão nessas mesmas dez nobres permanências. Todos aqueles que no presente permanecem em nobres permanências, permanecem nessas mesmas dez nobres permanências. </p>

<p class=Normal> “Essas são as dez nobres permanências nas quais os nobres permaneceram, permanecem e permanecerão.” </p>

