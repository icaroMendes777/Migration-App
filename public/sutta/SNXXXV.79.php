<p class=Normal> Então, um certo bhikkhu foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse: “Venerável senhor, há uma coisa que quando abandonada, a ignorância é abandonada por um bhikkhu e o verdadeiro conhecimento surge?" </p>

<p class=Normal>"Há uma coisa, bhikkhu, que quando abandonada, a ignorância é abandonada por um bhikkhu e o verdadeiro conhecimento surge." </p>

<p class=Normal>"E qual é essa coisa, venerável senhor?" </p>

<p class=Normal>"A ignorância, bhikkhu, é essa coisa que quando abandonada, a ignorância é abandonada por um bhikkhu e o verdadeiro conhecimento surge." [<a href="#N1">1</a>] </p>

<p class=Normal><a name=R1></a>"Mas, venerável senhor, como deve um bhikkhu compreender, como ele deve ver, para que a ignorância seja abandonada e surja o verdadeiro conhecimento?" </p>

<p class=Normal>"Bhikkhu, quando um bhikkhu compreende e vê o olho como impermanente, a ignorância é abandonada por ele e o verdadeiro conhecimento surge. Quando ele compreende e vê as formas como impermanentes ... Quando ele compreende e vê como impermanente qualquer sensação que surja tendo o contato na mente como condição ... a ignorância é abandonada por ele e o verdadeiro conhecimento surge. </p>

<p class=Normal>"Quando um bhikkhu assim compreende e vê, a ignorância é abandonada por ele e o verdadeiro conhecimento surge." </p>

