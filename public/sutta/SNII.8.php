<span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-tab-count:1'>            </span>Disperse os desejos sensuais, Oh!
Brâmane!<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-tab-count:1'>            </span>Sem ter abandonado os desejos
sensuais,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-tab-count:1'>            </span>um sábio não alcança a unidade.<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-tab-count:1'>            </span>“Para fazer o que deve ser feito,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-tab-count:1'>            </span>com determinação ele se esforça.<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-tab-count:1'>            </span>Pois a vida de um errante preguiçoso<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-tab-count:1'>            </span>só espalha mais pó.<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR style='mso-ansi-language:PT-BR'><span
