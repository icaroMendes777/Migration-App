<p class=Normal> Certa ocasião o Abençoado estava em Kosambi às margens do rio Ganges. O Abençoado viu um grande tronco de madeira sendo carregado pela correnteza do rio Ganges e se dirigiu aos bhikkhus assim: "Vocês vêm, bhikkhus, aquele grande tronco de madeira sendo carregado pela correnteza do rio Ganges?" </p>

<p class=Normal> "Sim, venerável senhor." </p>

<p class=Normal> "Bhikkhus, se aquele tronco não se desviar para a margem mais próxima, nem se desviar para a margem mais distante, não afundar no meio da correnteza, nem for levado até terra firme, nem for pego por seres humanos, não for pego por seres não humanos, não for pego num redemoinho, e não apodrecer internamente, então irá declivar, tender e se inclinar na direção do oceano. Por qual razão? Porque a correnteza do rio Ganges decliva, tende e se inclina ha direção do oceano. </p>

<p class=Normal> "Do mesmo modo, bhikkhus, se vocês não se desviarem para a margem mais próxima, nem se desviarem para a margem mais distante, não afundarem no meio da correnteza, nem forem jogados na terra firme, nem forem pegos por seres humanos, não forem pegos por seres não humanos, não forem pegos num redemoinho, e não apodrecerem internamente, então irá declivar, tender e se inclinar na direção de nibbana. Por qual razão? Porque o entendimento correto decliva, tende e se inclina ha direção de nibbana." </p>

<p class=Normal> Quando isso foi dito um certo bhikkhu perguntou ao Abençoado: "O que, venerável senhor, é a margem mais próxima? O que é a margem mais distante? O que é afundar no meio da correnteza? O que é ser jogado na terra firme? O que é ser pego por seres humanos? O que é ser pego por seres não humanos? O que é ser pego num redemoinho? O que é apodrecer internamente?" </p>

<p class=Normal> "'A margem mais próxima' é uma designação para as seis bases internas. 'A margem mais distante' é uma designação para as seis bases externas. 'Afundar no meio da correnteza' é uma designação para o deleite e cobiça. 'Ser jogado na terra firme' é uma designação para a presunção 'eu sou'. </p>

<p class=Normal> "O que, bhikkhus, é ser pego por seres humanos? Aqui alguém vive em companhia de pessoas leigas; ele se alegra com elas e se entristece com elas, ele está feliz quando elas estão felizes e triste quando elas estão tristes, ele se envolve nas suas tarefas e responsabilidades. Isso é chamado ser pego por seres humanos. </p>

<p class=Normal> "O que, bhikkhus, é ser pego por seres não humanos? Aqui alguém vive a vida santa aspirando pelo renascimento numa certa ordem de devas, pensando: 'Através desta virtude, ou voto, ou austeridade, ou vida santa, eu me tornarei um deva.' Isso é chamado ser pego por seres não humanos. </p>

<p class=Normal> "'Ser pego num redemoinho', essa bhikkhus é uma designação para os cinco elementos do prazer sensual. </p>

<p class=Normal> "O que, Kimbila, é 'apodrecer internamente'? Aqui Kimbila, um bhikkhu comete uma certa ofensa, uma ofensa para a qual não há reabilitação. Isso é chamado apodrecer internamente. </p>

