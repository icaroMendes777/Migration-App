<p class=Normal style='margin-left:.75in'>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Certa
ocasião, o Venerável Mahakaccana estava entre o povo de Avanti numa cabana na floresta
em Makkarakata. Então, um grande número de brâmanes jovens, discípulos do
brâmane Lohicca, enquanto juntavam lenha, se aproximaram da cabana do Venerável
Mahakaccana. Ao se aproximarem, eles pisotearam ruidosamente ao redor da
cabana, e de um modo buliçoso e ruidoso eles fizeram muitas travessuras,
dizendo: “Esses contemplativos carecas, lacaios, escuros, descendentes dos pés do
Ancestral, são honrados, respeitados, estimados, adorados e venerados pelos seus
devotos servis.” [<a href="#N1">1</a>]<o:p></o:p></span></p>


<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>Então, o Venerável Mahakaccana saiu da sua
moradia e disse para aqueles jovens brâmanes: “Não façam barulho, meninos. Eu
falarei com vocês sobre o Dhamma.” Quando isso foi dito, aqueles jovens ficaram
em silêncio. Então, o Venerável Mahakaccana se dirigiu aos jovens em versos:<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:PT-BR;
mso-bidi-font-weight:bold'>“Aqueles homens do passado que se distinguiam pela
virtude, <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>aqueles brâmanes que se recordavam das antigas
regras,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>com as suas portas dos sentidos guardadas, bem
protegidas,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>permaneciam com a raiva subjugada no
íntimo.<span style='mso-spacerun:yes'>  </span><br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>Eles se deliciavam com o Dhamma e a
meditação, [<a href="#N2">2</a>]<br>

<a name=R2></a><span lang=PT-BR
style='mso-ansi-language:PT-BR;mso-bidi-font-weight:bold'>aqueles brâmanes que
se recordavam das antigas regras. <o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>“Mas eles decaíram, e afirmam ‘Nós recitamos.’<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>Inflados pelo clã, vivendo na imoralidade,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>subjugados pela raiva, armados com diversas
armas,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>eles molestam ambos, fortes e fracos. <o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>“Para aquele com as portas dos sentidos
desguardadas<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[todos os votos que ele fez] são inúteis<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>como a fortuna que alguém ganha num sonho:<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>jejuar e dormir no chão,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>banhar-se ao amanhecer, [estudar] os três
Vedas,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>vestir-se com peles grosseiras, com os cabelos
emaranhados e sujos;<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>mantras, regras e votos, austeridades,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>hipocrisia, bengalas, abluções: <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>esses emblemas dos brâmanes<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>são usados para aumentar os seus ganhos
mundanos.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>“Uma mente bem concentrada,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>límpida e livre de máculas,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>compassiva com todos os seres sencientes<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>esse é o caminho para a união com Brahma.”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR;mso-bidi-font-weight:
bold'>Então, aqueles jovens brâmanes, furiosos e desgostosos, foram até o
brâmane Lohicca e disseram: “Veja, senhor, você deve saber que o contemplativo
Mahakaccana de modo categórico difama e ridiculariza os mantras dos brâmanes.”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR;mso-bidi-font-weight:
bold'>Quando isso foi dito, o brâmane Lohicca ficou furioso e desgostoso. Mas
então lhe ocorreu que: “Não é apropriado que eu abuse e insulte o contemplativo
Mahakaccana apenas com base naquilo que ouvi desses jovens. Devo ir até ele e
averiguar.”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR;mso-bidi-font-weight:
bold'>Então, o brâmane Lohicca, junto com aqueles jovens brâmanes, foram até o
Venerável Mahakaccana e eles</span><span lang=PT-BR style='mso-ansi-language:
PT-BR'> se cumprimentaram. Quando a conversa cortês e amigável havia terminado<span
style='mso-bidi-font-weight:bold'>, ele sentou a um lado e disse: “Mestre
Kaccana, um grande número de jovens brâmanes, meus discípulos, vieram até aqui
enquanto juntavam lenha?”<o:p></o:p></span></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR;mso-bidi-font-weight:
bold'>“Eles vieram, brâmane.”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR;mso-bidi-font-weight:
bold'>“O Mestre Kaccana teve alguma conversa com eles?”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR;mso-bidi-font-weight:
bold'>“Eu tive uma conversa com eles, brâmane.”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR;mso-bidi-font-weight:
bold'>“Que tipo de conversa você teve com eles, Mestre Kaccana?”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR;mso-bidi-font-weight:
bold'>“A conversa que tive com aqueles jovens foi assim:<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:PT-BR;
mso-bidi-font-weight:bold'>‘Aqueles homens do passado que se distinguiam pela
virtude,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>aqueles brâmanes que se recordavam das antigas
regras,<br> ...<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>compassiva com todos os seres sencientes<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>esse é o caminho para a união com Brahma.’<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR;mso-bidi-font-weight:
bold'>Assim foi a conversa que tive com aqueles jovens.”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR;mso-bidi-font-weight:
bold'>“O Mestre Kaccana disse ‘com as portas dos sentidos desguardadas.’ De que
modo, Mestre Kaccana, alguém está ‘com as portas dos sentidos desguardadas’?”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR;mso-bidi-font-weight:
bold'>“Aqui, brâmane, ao ver uma forma com o olho, uma pessoa se empenha pela
forma prazerosa e repele a forma des-prazerosa. [<a href="#N3">3</a>] <a
name=R3></a>Ela permanece sem estabelecer a atenção plena no corpo, com a mente
limitada, e não compreende como na verdade é a libertação da mente, a
libertação através da sabedoria, na qual aqueles estados ruins e prejudiciais cessam
sem deixar vestígio. Ao ouvir um som com o ouvido ... Ao cheirar um aroma com o nariz ... 
Ao saborear um sabor com a língua ... Ao tocar um tangível com o corpo ... Ao conscientizar um objeto
mental com a mente, uma pessoa se empenha pelo objeto mental prazeroso e repele
o objeto mental des-prazeroso. Ela permanece sem estabelecer a atenção plena no
corpo, com a mente limitada, e não compreende como na verdade é a libertação da
mente, a libertação através da sabedoria, na qual aqueles estados ruins e
prejudiciais cessam sem deixar vestígio.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR;mso-bidi-font-weight:
bold'>“É maravilhoso, Mestre Kaccana! É surpreendente, Mestre Kaccana! Como o
Mestre Kaccana declarou a pessoa cujas portas dos sentidos estão na verdade
desguardadas como alguém ‘com as portas dos sentidos desguardadas’! Mas o
Mestre Kaccana disse ‘com as portas dos sentidos guardadas.’ De que modo,
Mestre Kaccana, uma pessoa está ‘com as portas dos sentidos guardadas’?”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR;mso-bidi-font-weight:
bold'>“Aqui, brâmane, ao ver uma forma com o olho, uma pessoa não se empenha
pela forma prazerosa e não repele a forma des-prazerosa. Ela permanece com a
atenção plena no corpo estabelecida, com a mente ilimitada, e compreende como
na verdade é a libertação da mente, a libertação através da sabedoria, na qual
aqueles estados ruins e prejudiciais cessam sem deixar vestígio. Ao ouvir um som com o ouvido ... Ao cheirar um aroma com o nariz ... 
Ao saborear um sabor com a língua ... Ao tocar um tangível com o corpo ... Ao conscientizar um objeto mental com a mente, a pessoa não se
empenha pelo objeto mental prazeroso e não<span style='mso-spacerun:yes'> 
</span>repele o objeto mental <a name=PVW>des-prazeroso</a>. Ela permanece com
a atenção plena no corpo estabelecida, com a mente ilimitada, e compreende como
na verdade é a libertação da mente, a libertação através da sabedoria, na qual
aqueles estados ruins e prejudiciais cessam sem deixar vestígio.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR;mso-bidi-font-weight:
bold'>“É maravilhoso, Mestre Kaccana! É surpreendente, Mestre Kaccana! Como o
Mestre Kaccana declarou a pessoa cujas portas dos sentidos estão na verdade
guardadas como alguém ‘com as portas dos sentidos guardadas’! </span><span
lang=PT-BR style='mso-ansi-language:PT-BR'>Magnífico, Mestre Kaccana!
Magnífico, Mestre Kaccana! Mestre Kaccana esclareceu o Dhamma de várias formas,
como se tivesse colocado em pé o que estava de cabeça para baixo, revelasse o
que estava escondido, mostrasse o caminho para alguém que estivesse perdido ou
segurasse uma lâmpada no escuro para aqueles que possuem visão pudessem ver as
formas. Nós buscamos refúgio no Mestre Kaccana, no Dhamma e na Sangha dos
bhikkhus. Que o Mestre Kaccana me aceite como o discípulo leigo que
buscou refúgio para o resto da vida<span style='mso-bidi-font-weight:bold'>.<o:p></o:p></span></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR;mso-bidi-font-weight:
bold'>“Que o Mestre Kaccana se acerque da família Lohicca como ele se acerca
das famílias dos discípulos leigos em Makkarakata. Lá, os jovens brâmanes e as
donzelas<span style='mso-spacerun:yes'>  </span>homenagearão o Mestre Kaccana,
eles ficarão em pé por respeito, eles oferecerão para o Mestre Kaccana um
assento e água, e isso será para o bem estar e felicidade deles por muito
tempo.”</span><b style='mso-bidi-font-weight:normal'><span lang=PT-BR
style='mso-ansi-language:PT-BR'><o:p></o:p></span></b></p>

