<p class=Normal> Em Savatthi. "Bhikkhus, da ignorância como condição, as formações volitivas [surgem]. Das formações volitivas como condição, a consciência. Da consciência como condição, a mentalidade-materialidade (nome e forma) ... Essa é a origem de toda essa massa de sofrimento." </p>

<p class=Normal>Quando isso foi dito, um certo bhikkhu perguntou ao Abençoado: </p>

<p class=Normal>"Venerável senhor, o que é o envelhecimento e morte? A quem eles pertencem?" </p>

<p class=Normal>"Não é uma pergunta válida," o Abençoado respondeu. Dizer um dos dois, ‘O que é o envelhecimento e morte, a quem eles pertencem,’ ou ‘envelhecimento e morte são uma coisa, quem experimenta isso é outra,’ ambas afirmações são idênticas no significado; elas diferem apenas no fraseado. Quando existe o entendimento, ‘a alma e o corpo são a mesma coisa,’ não pode haver a vida santa; e quando existe o entendimento, ‘a alma e o corpo são duas coisas distintas,’ não pode haver a vida santa. Evitando esses dois extremos o Tathagata ensina o Dhamma pelo meio: ‘Do nascimento como condição, o envelhecimento e a morte.’"</p>

<p class=Normal>"Venerável senhor, nascimento … ser/existir … apego … desejo … sensação … contato … as bases dos sentidos … mentalidade-materialidade (nome e forma) … consciência… formações volitivas … O que é tudo isso? A quem tudo isso pertence?" </p>

<p class=Normal>"Não é uma pergunta válida," o Abençoado respondeu. Dizer um dos dois, ‘O que são as formações volitivas, a quem elas pertencem,’ ou ‘as formações volitivas são uma coisa, quem experimenta isso é outra,’ ambas afirmações são idênticas no significado; elas diferem apenas no fraseado. Quando existe o entendimento, ‘a alma e o corpo são a mesma coisa,’ não pode haver a vida santa; e quando existe o entendimento, ‘a alma e o corpo são duas coisas distintas,’ não pode haver a vida santa. Evitando esses dois extremos o Tathagata ensina o Dhamma pelo meio: ‘Da ignorância como condição, as formações volitivas.’</p>

<p class=Normal>"Mas com o desaparecimento e cessação sem deixar vestígios dessa mesma ignorância, qualquer tipo de contorção, manobra, vacilação que possa haver - ‘O que é o envelhecimento e morte, a quem eles pertencem,’ ou ‘envelhecimento e morte são uma coisa, quem experimenta isso é outra,’ ou ‘a alma e o corpo são a mesma coisa,’ ou ‘a alma e o corpo são duas coisas distintas,’ - tudo isso é abandonado, cortado pela raiz, feito como com um tronco de palmeira, eliminado de modo que não estará sujeito a um futuro surgimento. </p>

<p class=Normal>"Mas com o desaparecimento e cessação sem deixar vestígios dessa mesma ignorância, qualquer tipo de contorção manobra, vacilação que possa haver - ‘O que é o nascimento, a quem ele pertence,’ ou ‘nascimento é uma coisa, quem experimenta isso é outra,’ ... ‘O que são as formações volitivas, a quem elas pertencem,’ ou ‘as formações volitivas são uma coisa, quem experimenta isso é outra,’ou ‘a alma e o corpo são a mesma coisa,’ ou ‘a alma e o corpo são duas coisas distintas,’ - tudo isso é abandonado, cortado pela raiz, feito como com um tronco de palmeira, eliminado de modo que não estará sujeito a um futuro surgimento."</p>


