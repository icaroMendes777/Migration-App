<p class=Normal>Assim ouvi. Em certa ocasião o Abençoado estava em Savatthi, no palácio da mãe de Migara, no Parque do Oriente. Naquela ocasião Visakha, mãe de Migara, se encontrava envolvida em negociações com o rei Pasenadi de Kosala. Mas o rei não se mostrava disposto a convergir ao que Visakha intencionava.</p>



<p class=Normal>Então, Visakha, a mãe de Migara, foi até ao Abençoado no meio do dia, e após cumprimentá-lo, sentou a um lado. Enquanto ela assim se encontrava o Abençoado lhe disse: "Visakha, de onde você vem assim no meio do dia?”</p>



<p class=Normal>"Venerável senhor, eu me encontrava envolvida em negociações com o rei Pasenadi de Kosala. Mas o rei não se mostrava disposto a convergir ao que eu intencionava.</p>





<p class=Normal>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p>



	<p class=Normal style='margin-left:.75in'>"Toda dependência é dolorosa,

	toda independência é feliz.<br>

	Aquilo que é possuído em conjunto traz sofrimento,<br>

	difícil é transcender os jugos e vínculos.”</p>


