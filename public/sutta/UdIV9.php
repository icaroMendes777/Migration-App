<p class=Normal>Agora, naquela ocasião o venerável Upasena Vangantaputta se encontrava retirado, em reclusão, e a seguinte pensamento lhe ocorreu:</p>



<p class=Normal>"Que vantagem, que verdadeira vantagem é o fato de que o meu mestre é o Abençoado, um arahant, perfeitamente iluminado; de que eu abandonei a vida em família por um Dhamma e Vinaya bem ensinado; de que meus companheiros de vida santa são virtuosos e dotados de qualidades admiráveis; de que eu alcancei o ápice dos preceitos; de que minha mente é unificada e bem concentrada; de que eu seja um arahant, cujas impurezas foram destruídas; de que eu tenho vários poderes supra-humanos e grande autoridade. Feliz é a minha vida, feliz será a minha morte.”</p>



<p class=Normal>Então, conhecendo com sua mente o pensamento que ocorreu ao venerável Upasena Vangantaputta, o Abençoado nessa ocasião exclamou:</p>



	<p class=Normal style='margin-left:.75in'>"Aquele que não há do que se arrepender<br>

	com relação à vida ou com relação à morte,<br>

	se de fato iluminado,<br> 

	tendo conhecido o estado pacífico,<br>

	não sofre quando diante da lamentação.</p>

	

	<p class=Normal style='margin-left:.75in'>Pois naquele que destruiu a sede pela existência<br>

	- um bhikkhu cuja mente foi pacíficada -<br>

	tanto o nascimento quanto a perambulação <br>

	foram totalmente erradicados,<br>

	o ciclo de renascimentos encerrado.”</p>

