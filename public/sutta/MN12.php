Tathagatas…[<a href="#N10">10</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R10></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>15. (6) &quot;Outra vez, o Tathagata compreende como na verdade é, a
