um dos poderes dos Tathagatas…[<a href="#N8">8</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R8></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>13. (4) &quot;Outra vez, o Tathagata compreende como na verdade é, o
