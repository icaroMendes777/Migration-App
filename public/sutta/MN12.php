<p class=Normal><a name=R12></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>17. (8) &quot;Outra vez, o Tathagata se recorda das suas muitas vidas
passadas, isto é, um nascimento, dois nascimentos….(veja o <a href="MN4.php#T27">MN
