<p class=Normal><a name=N7></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[7] Esta frase é um tanto obscura mas de acordo com o Vbh, que oferece uma análise detalhada dos dez 
poderes do Tathagata, há vários tipos de kamma prejudicial que uma pessoa pode cometer e algumas vezes esse kamma 
