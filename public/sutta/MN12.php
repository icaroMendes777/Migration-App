chama de obstruções não são capazes de obstruir alguém que se ocupe com elas.'
E vendo nenhum fundamento nisso, eu permaneço seguro, destemido e intrépido.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>26.
&quot;Eu não vejo o fundamento com base no qual algum contemplativo… ou
