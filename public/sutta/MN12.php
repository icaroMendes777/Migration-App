dos poderes dos Tathagatas…[<a href="#N11">11</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R11></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>16. (7) &quot;Outra vez, o Tathagata compreende como na verdade é, a
impureza, a purificação, o surgimento dos jhanas, das libertações, das concentrações
