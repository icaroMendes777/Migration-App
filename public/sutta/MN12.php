assembléias e coloca em movimento a roda de Brahma.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>28.
&quot;Sariputta, sendo que eu assim sei e vejo, deveria alguém dizer a meu
respeito… ele acabará terminando no inferno.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(As Oito
