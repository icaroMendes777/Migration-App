modos e detalhes. Esse também é um dos poderes dos Tathagatas…<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18. (9)
&quot;Outra vez, com o olho divino que é purificado e sobrepuja o humano, o
Tathagata vê seres falecendo e renascendo, inferiores e superiores, bonitos e
feios, afortunados e desafortunados…(veja o <a href="MN4.php#T29">MN 4 verso 29</a>
)…e ele compreende como os seres continuam de acordo com as suas ações. Esse
também é um dos poderes dos Tathagatas…<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>19. (10)
&quot;Outra vez, realizando por si mesmo com conhecimento direto, o Tathagata
aqui e agora entra e permanece na libertação da mente e libertação através da
sabedoria que são imaculadas, com a destruição de todas as impurezas. Esse
também é um dos poderes dos Tathagatas que o Tathagata possui, pelo qual ele
reivindica o lugar de líder do rebanho, ruge o seu rugido de leão nas
assembléias e coloca em movimento a roda de Brahma.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>20. &quot;O
Tathagata possui esses dez poderes de um Tathagata, possuindo-os ele reivindica
o lugar de líder do rebanho, ruge o seu rugido de leão nas assembléias e coloca
em movimento a roda de Brahma.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>21.
&quot;Sariputta, sendo que eu assim sei e vejo, deveria alguém dizer a meu
respeito: 'O contemplativo Gotama não possui nenhum estado supra-humano,
nenhuma distinção em conhecimento e visão dignos dos nobres. O contemplativo
Gotama ensina um Dhamma que é a mera discussão de argumentos, seguindo a sua
própria linha de investigação de acordo com aquilo que lhe ocorra' - a menos
que ele abandone essa afirmação e esse estado mental e abdique dessa opinião,
ele acabará terminando no inferno. [<a href="#N13">13</a>] <a name=R13></a>Da
mesma forma que um bhikkhu possuído de virtude, concentração e sabedoria
desfrutaria aqui e agora do conhecimento supremo, assim também ocorrerá neste
caso, eu digo, que a menos que ele abandone essa afirmação e esse estado mental
e abdique dessa opinião, ele acabará terminando no inferno. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(Quatro
Tipos de Intrepidez ou Autoconfiança)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>22.
&quot;Sariputta, o Tathagata possui esses quatro tipos de intrepidez,
possuindo-os ele reivindica o lugar de líder do rebanho, ruge o seu rugido de
leão nas assembléias e coloca em movimento a roda de Brahma. Quais são os
quatro?<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>23.
&quot;Nesse caso, eu não vejo o fundamento com base no qual algum contemplativo
ou brâmane, ou deva, ou Mara, ou Brahma, ou qualquer outro no mundo pudesse, de
acordo com o Dhamma, acusar-me do seguinte: 'Apesar de reivindicar a completa
iluminação, você não é completamente iluminado com respeito a certas coisas, (<i>dhammas</i>).' E
vendo nenhum fundamento nisso, eu permaneço seguro, destemido e intrépido.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>24.
&quot;Eu não vejo o fundamento com base no qual algum contemplativo… ou
