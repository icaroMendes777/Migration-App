<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. (1)
&quot;Nesse caso, o Tathagata compreende como na verdade é, o possível como
possível e o impossível como impossível. [<a href="#N6">6</a>] <a name=R6></a>E
esse é um dos poderes dos Tathagatas que o Tathagata possui, pelo qual ele
reivindica o lugar de líder do rebanho, ruge o seu rugido de leão nas
assembléias e coloca em movimento a roda de Brahma.</span><span lang=PT-BR
style='font-size:11.0pt;mso-bidi-font-size:12.0pt;mso-ansi-language:PT-BR'><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><a name=T11></a>11. (2)
&quot;Outra vez, o Tathagata compreende como na verdade é, os resultados das
