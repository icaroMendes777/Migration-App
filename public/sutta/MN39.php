<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião o Abençoado estava em Anga numa cidade chamada Assapura.
Lá ele se dirigiu aos monges desta forma: “Bhikkhus”. – “Venerável Senhor,”
eles responderam. O Abençoado disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><a name=T2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2. “’Contemplativos, contemplativos,’ bhikkhus, assim é como as pessoas
os percebem. E quando vocês são perguntados, ‘O que vocês são?’, vocês afirmam
que são contemplativos. Visto que assim é como vocês são chamados e o que vocês
afirmam ser, vocês deveriam treinar da seguinte forma: Nós realizaremos e
praticaremos aquelas coisas que fazem de alguém um contemplativo, que fazem de
alguém um brâmane, [<a href="#N1">1</a>] <a name=R1></a>de tal modo que a nossa
designação seja verdadeira e a nossa afirmação genuína e de forma que a
generosidade daqueles cujos mantos, comida esmolada, moradia e medicamentos nós
utilizamos, lhes tragam grandes frutos e benefícios, para que a nossa vida
santa não seja em vão, mas frutífera e fértil.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><a name="T3"></a><b>(Conduta e
Modo de Vida)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. “E o
que, bhikkhus, são as coisas que fazem de alguém um contemplativo, que fazem de
alguém um brâmane? Bhikkhus, vocês deveriam treinar da seguinte forma: ‘Nós
estaremos possuídos de vergonha de cometer transgressões e temor de cometer
transgressões.’ [<a href="#N2">2</a>] <a name=R2></a>Agora, bhikkhus, vocês poderiam
pensar assim: ‘Nós estamos possuídos de vergonha de cometer transgressões e
temor de cometer transgressões. Esse tanto é o bastante, esse tanto foi feito,
o objetivo de um contemplativo foi alcançado, não há mais nada que devamos
fazer’; e vocês descansarão satisfeitos com esse tanto. Bhikkhus, eu lhes
informo, eu lhes declaro: Vocês que buscam o status de um contemplativo, não
fiquem aquém do objetivo da contemplação quando há mais a ser feito. [<a
href="#N3">3</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>4. “Que mais há para ser feito? Bhikkhus, vocês deveriam treinar da
seguinte forma: ‘Nossa conduta corporal será purificada, límpida e visível,
impecável e contida, e nós não elogiaremos a nós mesmos e criticaremos os
outros por conta dessa conduta corporal purificada.’ Agora, bhikkhus, vocês
poderiam pensar assim: ‘Nós estamos possuídos de vergonha de cometer
transgressões e temor de cometer transgressões e a nossa conduta corporal foi
purificada. Esse tanto é o bastante, esse tanto foi feito, o objetivo de um
contemplativo foi alcançado, não há mais nada que devamos fazer’; e vocês
descansarão satisfeitos com esse tanto. Bhikkhus, eu lhes informo, eu lhes
declaro: Vocês que buscam o status de um contemplativo, não fiquem aquém do
objetivo da contemplação quando há mais a ser feito.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. “Que
mais há para ser feito? Bhikkhus, vocês deveriam treinar da seguinte forma:
‘Nossa conduta verbal será purificada, límpida e visível, impecável e contida,
e nós não elogiaremos a nós mesmos e criticaremos os outros por conta dessa
conduta verbal purificada.’ Agora, bhikkhus, vocês poderiam pensar assim: ‘Nós
estamos possuídos de vergonha de cometer transgressões e temor de cometer
transgressões e a nossa conduta corporal foi purificada e a nossa conduta
verbal foi purificada . Esse tanto é o bastante, esse tanto foi feito, o
objetivo de um contemplativo foi alcançado, não há mais nada que devamos
fazer’; e vocês descansarão satisfeitos com esse tanto. Bhikkhus, eu lhes
informo, eu lhes declaro: Vocês que buscam o status de um contemplativo, não
fiquem aquém do objetivo da contemplação quando há mais a ser feito.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. “Que
mais há para ser feito? Bhikkhus, vocês deveriam treinar da seguinte forma:
‘Nossa conduta mental será purificada, límpida e visível, impecável e contida,
e nós não elogiaremos a nós mesmos e criticaremos os outros por conta dessa
conduta mental purificada.’ Agora, bhikkhus, vocês poderiam pensar assim: ‘Nós
estamos possuídos de vergonha de cometer transgressões e temor de cometer
transgressões e a nossa conduta corporal foi purificada, a nossa conduta verbal
foi purificada e a nossa conduta mental foi purificada . Esse tanto é o
bastante...’; e vocês descansarão satisfeitos com esse tanto. Bhikkhus, eu lhes
informo, eu lhes declaro: Vocês que buscam o status de um contemplativo, não
fiquem aquém do objetivo da contemplação quando há mais a ser feito.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. “Que
mais há para ser feito? Bhikkhus, vocês deveriam treinar da seguinte forma:
‘Nosso modo de vida será purificado, límpido e visível, impecável e contido, e
nós não elogiaremos a nós mesmos e criticaremos os outros por conta desse modo
de vida purificado.’ Agora, bhikkhus, vocês poderiam pensar assim: ‘Nós estamos
possuídos de vergonha de cometer transgressões e temor de cometer transgressões
e a nossa conduta corporal, verbal e mental foram purificadas e o nosso modo de
vida foi purificado. Esse tanto é o bastante...’; e vocês descansarão
satisfeitos com esse tanto. Bhikkhus, eu lhes informo, eu lhes declaro: Vocês
que buscam o status de um contemplativo, não fiquem aquém do objetivo da
contemplação quando há mais a ser feito.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><a name="T8"></a><b>(Contenção
dos Sentidos)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>8. “Que mais há para ser feito? Bhikkhus, vocês deveriam treinar da
seguinte forma: ‘Guardaremos as portas dos nossos meios dos sentidos. Ao ver
uma forma com o olho, nós não nos agarraremos aos seus sinais ou detalhes.
Visto que, se permanecermos com a faculdade do olho descuidada, seremos tomados
pelos estados ruins e prejudiciais de cobiça e tristeza. Praticaremos a
contenção, protegeremos a faculdade do olho, nos empenharemos na contenção da
faculdade do olho. Ao ouvir um som com o ouvido ... Ao cheirar um aroma com o
nariz<span style='mso-spacerun:yes'>  </span>… Ao saborear um sabor com a
língua … Ao tocar algo tangível com o corpo … Ao conscientizar um objeto
mental<span style='mso-spacerun:yes'>  </span>com a<span
style='mso-spacerun:yes'>  </span>mente, nós não nos agarraremos aos seus
sinais ou detalhes. Visto que, se permanecermos com a faculdade da mente
descuidada, seremos tomados pelos estados ruins e prejudiciais de cobiça e
tristeza. Praticaremos a contenção, protegeremos a faculdade da mente, nos
empenharemos na contenção da faculdade da mente. Agora, bhikkhus, vocês
poderiam pensar assim: ‘Nós estamos possuídos de vergonha de cometer
transgressões e temor de cometer transgressões e a nossa conduta corporal,
verbal e mental foram purificadas, o nosso modo de vida foi purificado e nós
guardamos as portas dos nossos meios dos sentidos. Esse tanto é o bastante
...’; e vocês descansarão satisfeitos com esse tanto. Bhikkhus, eu lhes
informo, eu lhes declaro: Vocês que buscam o status de um contemplativo, não
fiquem aquém do objetivo da contemplação quando há mais a ser feito. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(Moderação
no Comer)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. “Que
mais há para ser feito? Bhikkhus, vocês deveriam treinar da seguinte forma:
‘Seremos moderados no comer. Refletindo de maneira sábia, o alimento não deve
ser tomado como forma de diversão ou para embriaguez, tampouco com o objetivo
de embelezamento e para ser mais atraente; mas somente com o propósito de
manter a resistência e continuidade deste corpo, como forma de dar um fim ao
desconforto e para auxiliar a vida santa. Considerando: “Dessa forma darei um
fim às antigas sensações (de fome) sem despertar novas sensações (de comida em
excesso) e serei saudável e sem culpa e viverei em comodidade.”’ Agora,
bhikkhus, vocês poderiam pensar assim: ‘Nós estamos possuídos de vergonha de
cometer transgressões e temor de cometer transgressões e a nossa conduta
corporal, verbal e mental foram purificadas, o nosso modo de vida foi
purificado, nós guardamos as portas dos nossos meios dos sentidos e somos
moderados no comer. Esse tanto é o bastante ...’; e vocês descansarão
satisfeitos com esse tanto. Bhikkhus, eu lhes informo, eu lhes declaro: Vocês
que buscam o status de um contemplativo, não fiquem aquém do objetivo da
contemplação quando há mais a ser feito.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(Vigilância)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. “Que
mais há para ser feito? Bhikkhus, vocês deveriam treinar da seguinte forma:
‘Seremos dedicados à vigilância. Durante o dia, enquanto estivermos caminhando
para lá e para cá e sentados, purificaremos a nossa mente dos estados
obstrutivos. Na primeira vigília da noite, enquanto estivermos caminhando para
lá e para cá e sentados, purificaremos a nossa mente dos estados obstrutivos.
Na segunda vigília da noite nos deitaremos para dormir, no nosso lado direito,
na postura do leão com um pé sobre o outro, atentos e plenamente conscientes,
após anotar na nossa mente o horário para levantar. Após levantar,<span
style='mso-spacerun:yes'>  </span>na terceira vigília da noite, enquanto
estivermos caminhando para cá e para lá e sentados, purificaremos a nossa mente
dos estados obstrutivos.’ Agora, bhikkhus, vocês poderiam pensar assim: ‘Nós
estamos possuídos de vergonha de cometer transgressões e temor de cometer
transgressões e a nossa conduta corporal, verbal e mental foram purificadas, o
nosso modo de vida foi purificado, nós guardamos as portas dos nossos meios dos
sentidos, somos moderados no comer e somos dedicados à vigilância. Esse tanto é
o bastante ...’; e vocês descansarão satisfeitos com esse tanto. Bhikkhus, eu
lhes informo, eu lhes declaro: Vocês que buscam o status de um contemplativo,
não fiquem aquém do objetivo da contemplação quando há mais a ser feito.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(Atenção
Plena e Consciência Plena)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. “Que
mais há para ser feito? Bhikkhus, vocês deveriam treinar da seguinte forma:
estaremos possuídos de atenção plena e consciência plena. Agiremos com plena
consciência ao ir para a frente e retornar; agiremos com plena consciência ao
olhar para frente e desviar o olhar; agiremos com plena consciência ao dobrar e
estender os membros; agiremos com plena consciência ao carregar o manto externo,
o manto superior, a tigela; agiremos com plena consciência ao comer, beber,
mastigar e saborear; agiremos com plena consciência ao urinar e defecar;
agiremos com plena consciência ao caminhar, ficar em pé, sentar, dormir,
acordar, falar e permanecer em silêncio. Agora, bhikkhus, vocês poderiam pensar
assim: ‘Nós estamos possuídos de vergonha de cometer transgressões e temor de
cometer transgressões e a nossa conduta corporal, verbal e mental foram
purificadas, o nosso modo de vida foi purificado, nós guardamos as portas dos
nossos meios dos sentidos, somos moderados no comer, somos dedicados à
vigilância e somos possuídos de atenção plena e consciência plena. Esse tanto é
o bastante ...’; e vocês descansarão satisfeitos com esse tanto. Bhikkhus, eu
lhes informo, eu lhes declaro: Vocês que buscam o status de um contemplativo,
não fiquem aquém do objetivo da contemplação quando há mais a ser feito.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(Abandonando
os Obstáculos)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. Que
mais há para ser feito? Aqui, bhikkhus, um bhikkhu busca um lugar isolado: na
floresta, à sombra de uma árvore, uma montanha, uma ravina, uma caverna em uma
encosta, um cemitério, um matagal, um espaço aberto, uma cabana vazia.<o:p></o:p></span></p>



<p class=Normal><a name=T13></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>13. “Depois de esmolar alimentos, após a refeição, ele senta com as
pernas cruzadas, com o corpo ereto colocando a atenção plena à sua frente.
Abandonando a cobiça pelo mundo, ele permanece com a mente livre de cobiça; ele
purifica a sua mente da cobiça. Abandonando a má vontade, ele
permanece com a mente livre de má vontade, com compaixão pelo bem estar de
todos os seres vivos; ele purifica a sua mente da má vontade.
Abandonando a preguiça e o torpor, ele permanece livre da preguiça e do torpor,
perceptivo à luz, atento e plenamente consciente; ele purifica sua mente da
preguiça e do torpor. Abandonando a inquietação e a ansiedade, ele permanece
calmo com a mente em paz; ele purifica sua mente da inquietação e da ansiedade.
Abandonando a dúvida, ele assim permanece tendo superado a dúvida, sem
perplexidade em relação a qualidades mentais hábeis; ele purifica a mente da
dúvida.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14. “Bhikkhus,
&quot;Suponha que um homem, tomando um empréstimo, invista no seu negócio. Os
seu negócios vão bem. Ele paga a dívida e sobra algo para sustentar sua esposa.
O pensamento lhe ocorreria, ‘Antes, tomei um empréstimo, investi no meu
negócio. Agora o meu negócio foi bem. Eu paguei a dívida e sobrou algo para
sustentar minha esposa’. Por causa disso ele experimentaria alegria e
felicidade. Ou suponha que um homem se enferme – com dores e seriamente doente.
Ele não tolera a comida e não há força no seu corpo. Conforme o tempo passa,
ele finalmente se recupera dessa enfermidade. Ele tolera a comida e há força no
seu corpo. O pensamento lhe ocorreria, ‘Antes, eu estava doente ... Agora estou
recuperado daquela doença. Eu tolero a minha comida, há força no meu corpo.’
Por causa disso ele experimentaria alegria e felicidade. Ou suponha que um
homem está na prisão. Conforme o tempo passa, ele finalmente é libertado dessa
prisão, são e salvo, sem perda de patrimônio. O pensamento lhe ocorreria,
‘Antes, eu estava na prisão. Agora estou livre da prisão, são e salvo, sem
perda do meu patrimônio.’ Por causa disso ele experimentaria alegria e
felicidade. Ou suponha que um homem é um escravo, sujeito a outros, não sujeito
a si mesmo, incapaz de ir aonde queira. Conforme o tempo passa, ele finalmente
é libertado daquela escravidão, sujeito a si mesmo, não sujeito a outros,
livre, capaz de ir aonde queira. O pensamento lhe ocorreria, ‘Antes, eu era um
escravo… Agora estou livre daquela escravidão, sujeito a mim mesmo, não sujeito
a outros, livre, capaz de ir aonde queira.’ Por causa disso ele experimentaria
alegria e felicidade. Ou suponha que um homem, carregando dinheiro e
mercadorias, está viajando por uma estrada numa região desolada. Conforme o
tempo passa, ele finalmente emerge daquela região desolada, são e salvo, sem
perda de patrimônio. O pensamento lhe ocorreria, ‘Antes, carregando dinheiro e
mercadorias, eu estava viajando por uma estrada em uma região desolada. Agora
emergi dessa região desolada, são e salvo, sem perda do meu patrimônio.’ Por
causa disso ele experimentaria alegria e felicidade. Da mesma forma, quando
esses cinco obstáculos não são abandonados por ele, o bhikkhu os considera como
uma dívida, uma enfermidade, uma prisão, a escravidão, uma estrada através de
uma região desolada. Porém, quando esses cinco obstáculos são abandonados, ele
os considera como não ter dívidas, ter boa saúde, estar livre da prisão, estar
livre, estar num lugar com segurança. [<a href="#N4">4</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'><b>(Os Quatro Jhanas)</b><o:p></o:p></span></p>



<p class=Normal><a name=T15></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>15. “Tendo assim abandonado esses 5 obstáculos, imperfeições da mente
que enfraquecem a sabedoria, um bhikkhu, afastado dos prazeres sensuais, afastado das qualidades não hábeis, entra e permanece no primeiro jhana, que é caracterizado pelo pensamento aplicado e sustentado, com o êxtase e felicidade nascidos do afastamento. Ele permeia e impregna, cobre e preenche o corpo com o êxtase e felicidade nascidos do afastamento. Não há nada em todo o corpo que não esteja permeado pelo êxtase e felicidade nascidos do afastamento. É como se um banhista habilidoso ou seu aprendiz vertesse pó de banho numa bacia de latão e o misturasse, borrifando com água de tempos em tempos, de forma que essa bola de pó de banho - saturada, carregada de umidade, permeada por dentro e por fora - no entanto não pingasse; assim, o bhikkhu permeia, cobre e preenche o corpo com o êxtase e felicidade nascidos do afastamento.... <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16. &quot;E além disso,
abandonando o pensamento aplicado e sustentado, um bhikkhu entra e permanece no segundo jhana, que é caracterizado pela segurança interna e perfeita unicidade da mente, sem o pensamento aplicado e sustentado, com o êxtase e felicidade nascidos da concentração. Ele permeia e impregna, cobre e preenche o corpo com o êxtase e felicidade nascidos da concentração. Não há nada em todo o corpo que não esteja permeado pelo êxtase e felicidade nascidos da concentração. Como um lago sendo alimentado por uma fonte de água interna, não tendo um fluxo de água do leste, oeste, norte, ou sul, nem os céus periodicamente fornecendo chuvas abundantes, de modo que a fonte de água interna permeia e impregna, cobre e preenche o lago de água fresca, sem que nenhuma parte do lago não esteja permeada pela água fresca; assim também o bhikkhu permeia e impregna, cobre e preenche o corpo com o êxtase e felicidade nascidos da concentração...<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17. &quot;E além disso, abandonando o êxtase, um bhikkhu entra e permanece no terceiro jhana que é caracterizado pela felicidade sem o êxtase, acompanhada pela atenção plena, plena consciência e equanimidade, acerca do qual os nobres declaram: ‘Ele permanece numa estada feliz, equânime e plenamente atento.’ Ele permeia e impregna, cobre e preenche o corpo com a felicidade despojada do êxtase, de forma que não exista nada em todo o corpo que não esteja permeado com a felicidade despojada do êxtase. Como num lago que tenha flores de lótus azuis, brancas ou vermelhas, podem existir algumas flores de lótus azuis, brancas, ou vermelhas que, nascidas e tendo crescido na água, permanecem imersas na água e florescem sem sair de dentro da água, de forma que elas permanecem permeadas e impregnadas, cobertas e preenchidas com água fresca da raiz até a ponta, e nada dessas flores de lótus azuis, brancas ou vermelhas permanece sem estar permeado pela água fresca; assim também o bhikkhu permeia e impregna, cobre e preenche o corpo com a felicidade despojada de êxtase....<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18. &quot;E além disso, com o completo desaparecimento da felicidade, um bhikkhu entra e permanece no quarto jhana, que possui nem felicidade nem sofrimento, com a atenção plena e a equanimidade purificadas. Ele permanece permeando o corpo com a mente pura e luminosa, de forma que não exista nada em todo o corpo que não esteja permeado pela mente pura e luminosa. Como se um homem estivesse enrolado da cabeça aos pés com um tecido branco de forma que não houvesse nenhuma parte do corpo que não estivesse coberta pelo tecido branco; assim também o bhikkhu permanece permeando o corpo com a mente pura e luminosa. Não há nada no corpo que não esteja permeado por essa mente pura e luminosa.&quot;<o:p></o:p></span></p>

