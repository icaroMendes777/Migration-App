<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>19. &quot;Com a sua mente
dessa forma concentrada, purificada, luminosa, pura, imaculada, livre de
defeitos, flexível, maleável, estável e<span style='mso-spacerun:yes'>  </span>atingindo
a imperturbabilidade, ele a dirige para o conhecimento da recordação de vidas
passadas. Ele se recorda das suas muitas vidas passadas, isto é, um nascimento,
dois nascimentos, três nascimentos, quatro, cinco, dez, vinte, trinta,
quarenta, cinqüenta, cem, mil, cem mil, muitos éons de contração,
muitos éons de expansão, muitos éons de contração e
expansão, ‘Lá eu tinha tal nome, pertencia a tal clã, tinha tal aparência.
Assim era o meu alimento, assim era a minha experiência de prazer e dor, assim
foi o fim da minha vida. Falecendo daquele estado, eu ressurgi ali. Ali eu
também tinha tal nome, pertencia a tal clã, tinha tal aparência. Assim era o
meu alimento, assim era a minha experiência de prazer e dor, assim foi o fim da
minha vida. Falecendo daquele estado, eu ressurgi aqui.’ Assim ele se recorda
das suas muitas vidas passadas nos seus modos e detalhes. Tal como se um homem
fosse do seu vilarejo a um outro vilarejo e desse vilarejo a mais um outro
vilarejo e, então, desse vilarejo de volta ao vilarejo onde ele mora. O
pensamento lhe ocorreria, ‘Eu fui do meu vilarejo para aquele vilarejo ali. Ali
eu fiquei em pé de tal forma, sentei de tal forma, falei de tal forma e
permaneci em silêncio de tal forma. Daquele vilarejo eu fui para o outro
vilarejo lá e lá eu fiquei em pé de tal forma, sentei de tal forma, falei de
tal forma e permaneci em silêncio de tal forma. Desse vilarejo eu voltei para
casa.’ Da mesma<span style='mso-spacerun:yes'>  </span>forma - com a sua mente
assim concentrada, purificada, luminosa, pura, imaculada, livre de defeitos,
flexível, maleável, estável e atingindo a imperturbabilidade – o bhikkhu a
dirige e a inclina para o conhecimento da recordação de vidas passadas<span
style='mso-spacerun:yes'>  </span>Ele se recorda das suas muitas vidas
passadas...nos seus modos e detalhes.<o:p></o:p></span></p>

<p class=Normal><a name=T20></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>20. &quot;Com a<span style='mso-spacerun:yes'>  </span>sua mente dessa
forma concentrada, purificada, luminosa, pura, imaculada, livre de defeitos,
flexível, maleável, estável e atingindo a imperturbabilidade, ele a dirige para
o conhecimento do falecimento e ressurgimento dos seres. Por meio do olho
divino, que é purificado e sobrepuja o humano, ele vê seres falecendo e
renascendo, inferiores e superiores, bonitos e feios, afortunados e
desafortunados. Ele compreende como os seres prosseguem de acordo com as suas
ações desta forma: ‘Esses seres – dotados de má conduta com o corpo, linguagem
e mente, que insultam os nobres, com o entendimento incorreto e realizando
ações sob a influência do entendimento incorreto – com a dissolução do corpo,
após a morte, renasceram num estado de privação, num destino infeliz, nos
reinos inferiores, até mesmo no <span style='mso-bidi-font-weight:bold'>inferno</span>.
Porém estes seres - dotados de boa conduta com o corpo, linguagem e mente, que
não insultam os nobres, com o entendimento correto e realizando ações sob a
influência do entendimento correto – com a dissolução do corpo, após a morte,
renasceram num bom destino, no paraíso.’ Dessa forma - por meio do olho divino,
que é purificado e sobrepuja o humano, ele vê seres falecendo e renascendo,
inferiores e superiores, bonitos e feios e ele compreende como os seres
continuam de acordo com as suas ações. Tal como se houvessem duas casas com
portas e um homem com boa visão parado entre elas visse as pessoas entrando nas
casas e saindo, indo e vindo. Da mesma forma - com a sua mente assim
concentrada, purificada e luminosa, pura, imaculada, livre de defeitos,
flexível, maleável, estável e atingindo a imperturbabilidade – o bhikkhu a
dirige para o conhecimento do falecimento e ressurgimento dos seres. Por meio
do olho divino, que é purificado e sobrepuja o humano, ele vê seres falecendo e
renascendo, inferiores e superiores, bonitos e feios, afortunados e
desafortunados.... de acordo com as suas ações.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>21. &quot;Com a sua mente
dessa forma concentrada, purificada, luminosa, pura, imaculada, livre de
defeitos, flexível, maleável, estável e atingindo a imperturbabilidade, ele a
dirige para o conhecimento do fim das impurezas mentais. Ele compreende como na
verdade é que: ‘Isto é sofrimento ... Esta é a origem do sofrimento ... Esta é
a cessação do sofrimento ... Este é o caminho que conduz à cessação do
sofrimento ... Essas são as impurezas mentais ... Esta é a origem das impurezas
... Esta é a cessação das impurezas ... Este é o caminho que conduz à cessação
das impurezas.’ Ao conhecer e ver a sua mente está livre da impureza da
sensualidade, da impureza de ser/existir, da impureza da ignorância. Quando ela
está libertada surge o conhecimento, ‘Libertada.’ Ele compreende que ‘O
nascimento foi destruído, a vida santa foi vivida, o que deveria ser feito foi
feito, não há mais vir a ser a nenhum estado.’ Tal como se houvesse uma lagoa
num vale em uma montanha<span style='mso-spacerun:yes'>  </span>- clara,
límpida e cristalina – em que um homem com boa visão, em pé na margem, pudesse
ver conchas, cascalho e seixos e também cardumes de peixes nadando e descansando,
isso lhe ocorreria, ‘Esta lagoa tem a água clara, límpida e cristalina. Ali
estão aquelas conchas, cascalho e seixos e também aqueles cardumes de peixes
nadando e descansando.’ Da mesma forma - com a sua mente assim concentrada,
purificada, luminosa, pura, imaculada, livre de defeitos, flexível, maleável,
estável e atingindo a imperturbabilidade – o bhikkhu a dirige e a inclina para
o conhecimento do fim das impurezas mentais. Ele compreende como na verdade é
que: ‘Isto é sofrimento ... Esta é a origem do sofrimento ... Esta é a cessação
do sofrimento ... Este é o caminho que conduz à cessação do sofrimento ...
Essas são impurezas mentais ... Esta é a origem das impurezas ... Esta é a
cessação das impurezas ... Este é o caminho que conduz à cessação das
impurezas.’ Ao conhecer e ver,<span style='mso-spacerun:yes'>  </span>a sua
mente está livre da impureza do desejo sensual, da impureza de ser/existir, da
impureza da ignorância. Quando ela está libertada surge o conhecimento,
‘Libertada.’ Ele compreende que ‘O nascimento foi destruído, a vida santa foi
vivida, o que deveria ser feito foi feito, não há mais vir a ser a nenhum
estado.’<o:p></o:p></span></p>

