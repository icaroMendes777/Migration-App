<p class=Normal>Ajaan Brahmali faz o seguinte comentário sobre este sutta:</p> 

<p class=Normal>"<i>Sabbe dhamma</i> (todos os fenômenos) é uma referência à nossa experiência pessoal, da seguinte forma: todos os elementos da nossa experiência estão enraizados no desejo com o sentido que nós existimos devido ao desejo. Os fenômenos passam a existir devido à atenção significando que apenas experimentamos aquilo que damos atenção. Eles se originam do contato porque sem o contato não há nenhuma experiência. Eles convergem na sensação porque a sensação é o aspecto mais importante da nossa experiência, o fator motivacional em tudo que fazemos. Eles são presididos pela concentração porque essa é uma das cinco faculdades (<i>indriya</i>), sendo que todos os fenômenos seguem a sua liderança. A atenção plena é um princípio dominante porque é outra das cinco faculdades que nos dirige para aquilo que experimentamos." </p>

