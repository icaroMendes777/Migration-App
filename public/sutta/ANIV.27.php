<p class=Normal> "Bhikkhus, há essas quatro insignificâncias, ganhas facilmente e não passíveis de crítica. Quais quatro? </p>

<p class=Normal><a name=R1></a> (1) "Um manto feito de trapos é uma insignificância entre os mantos, ganho facilmente e não passível de crítica. (2) Um torrão de comida esmolada é uma insignificância entre as comidas, ganho facilmente e não passível de crítica. (3) A sombra de uma árvore é uma insignificância entre as pousadas, ganha facilmente e não passível de crítica. (4) Urina pútrida é uma insignificância entre os medicamentos, ganha facilmente e não passível de crítica. [<a href="#N1">1</a>] </p>

<p class=Normal>"Essas são as quatro insignificâncias, ganhas facilmente e não passíveis de crítica. Quando um bhikkhu está satisfeito com aquilo que é uma insignificância, ganha facilmente, eu digo, que ele possui um dos fatores da vida contemplativa." </p>

<p class=Normal>Quando alguém está satisfeito com aquilo que não é passível de crítica,<br>
uma insignificância ganha facilmente; <br>
quando a sua mente não está estressada<br>
por uma moradia, <br>
mantos, bebidas, e comida, <br>
ele não enfrenta nenhum obstáculo. </p>

<p class=Normal>Esses qualidades, corretamente ditas<br>
fazerem parte da vida contemplativa, <br>
são obtidas por um bhikkhu<br>
que está satisfeito e diligente.</p>

