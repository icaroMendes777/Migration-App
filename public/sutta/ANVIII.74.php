<p class=Normal> Ouvi que certa vez o Abençoado estava em Nadika na Casa de Tijolos. Lá ele se dirigiu aos bhikkhus: </p>

<p class=Normal> “Bhikkhus, atenção plena na morte, quando desenvolvida e cultivada, traz grandes frutos e grandes benefícios. Ela mergulha no Imortal, possui o Imortal como seu objetivo final. E como a atenção plena na morte é desenvolvida e cultivada para que traga grandes frutos e grandes benefícios, mergulhe no Imortal, e tenha o Imortal como seu objetivo final? </p>

<p class=Normal> "É o caso em que um bhikkhu, terminando o dia e chegando a noite, reflete: ‘Muitas são as [possíveis] causas da minha morte. (1) Uma cobra pode me picar, um escorpião pode me ferroar, uma centopéia pode me morder; (2) tropeçando, eu posso cair; (3) minha comida, digerida, pode me causar problemas; (4) minha bílis pode ser provocada; (5) minha fleuma pode ser provocada; (6) forças perfurantes do vento [no corpo] podem ser provocadas; (7) seres humanos podem me atacar; (8) seres não humanos podem me atacar. Assim é como a minha morte poderia acontecer. Isso seria um impedimento para mim.’ </p>

<p class=Normal> "Então o bhikkhu deveria investigar: ‘Existem qualidades mentais inábeis e prejudiciais que não foram abandonadas por mim e que seriam um impedimento para mim caso eu morresse durante a noite?’ Se, refletindo, ele se dá conta que qualidades mentais inábeis e prejudiciais não abandonadas por ele seriam um impedimento caso ele morresse durante a noite, então ele deve empregar desejo adicional, empenho, diligência, esforço, atenção plena e plena consciência para o abandono dessas mesmas qualidades inábeis e prejudiciais. Tal como se o turbante ou o cabelo de uma pessoa estivesse em chamas ela empregaria desejo adicional, empenho, diligência, esforço, atenção plena e plena consciência para apagar o fogo do seu turbante ou cabelo, da mesma forma o bhikkhu deveria empregar desejo adicional, empenho, diligência, esforço, atenção plena e plena consciência para o abandono dessas mesmas qualidades inábeis e prejudiciais. </p>

<p class=Normal> "Porém, se refletindo ele se dá conta que não há qualidades inábeis e prejudiciais que não foram abandonadas por ele que seriam um impedimento se ele morresse durante a noite, então por essa mesma razão ele deve permanecer em êxtase e felicidade, treinando dia e noite nas qualidades hábeis. </p>

<p class=Normal> "Além disso, há o caso em que um bhikkhu, terminando a noite e chegando o dia, reflete: ‘Muitas são as [possíveis] causas da minha morte. Uma cobra pode me picar, ... seres não humanos podem me atacar. Assim é como a minha morte poderia acontecer. Isso seria um impedimento para mim.’ Então o bhikkhu deveria investigar. ‘Existem qualidades mentais inábeis e prejudiciais que não foram abandonadas por mim e que seriam um impedimento para mim caso eu morresse durante o dia?’ Se, refletindo, ele se dá conta de que qualidades mentais inábeis e prejudiciais não abandonadas por ele seriam um impedimento caso ele morresse durante o dia, então ele deve empregar desejo adicional, empenho, diligência, esforço, atenção plena e plena consciência para o abandono dessas mesmas qualidade inábeis e prejudiciais. Tal como se o turbante ou o cabelo de uma pessoa estivesse em chamas ela empregaria desejo adicional, empenho, diligência, esforço, atenção plena e plena consciência para apagar o fogo do seu turbante ou cabelo, da mesma forma o bhikkhu deveria empregar desejo adicional, empenho, diligência, esforço, atenção plena não dividida e vigilância para o abandono dessas mesmas qualidades inábeis e prejudiciais. Porém, se refletindo ele se dá conta que não há qualidades inábeis e prejudiciais que não foram abandonadas por ele que seriam um impedimento se ele morresse durante o dia, então por essa mesma razão ele deve permanecer em êxtase e felicidade, treinando dia e noite nas qualidades hábeis. </p>

<p class=Normal> "Dessa forma, bhikkhus, é como a atenção plena na morte é desenvolvida e cultivada de forma a trazer grandes frutos e grandes benefícios. Ela mergulha no Imortal, possui o Imortal como seu objetivo final."
</p>

