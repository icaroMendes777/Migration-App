<p class=Normal>Assim ouvi. Em certa ocasião, o Abençoado estava em Rajagaha, no Bambual, no Santuário dos Esquilos. Naquela ocasião, o Venerável Maha Kassapa estava na Caverna Pipphali – doente, aflito, gravemente enfermo. E quando o venerável Maha Kassapa se recuperou, lhe ocorreu o seguinte pensamento: “E se eu for para Rajagaha esmolar alimentos?”</p>



<p class=Normal>Naquela ocasião, uma grande quantidade de devas – uns quinhentos devas – se encontravam ansiosos pela oportunidade de oferecer alimentos para o Venerável Maha Kassapa. Mas o Venerável Maha Kassapa, tendo recusado a oferenda destes quinhentos devas, bem cedo pela manhã ajustou seu manto interno e – carregando a sua tigela e mantos – foi para Rajagaha esmolar alimentos, seguindo pelas ruas habitadas por aqueles pobres e carentes, dedicados à atividade de tecelagem.</p>



<p class=Normal>O Abençoado viu o Venerável Maha Kassapa esmolando alimentos em Rajagaha, seguindo pelas ruas habitadas por aqueles pobres e carentes, dedicados à atividade de tecelagem.</p>



<p class=Normal>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p>



    <p class=Normal style='margin-left:.75in'>“Desapegado e desassociado, incógnito e anônimo,<br>

    treinado e estabelecido no que é essencial.<br>

    Com uma mente livre das impurezas que amadurecem<br> e conduzem ao futuro sofrimento.<br>

    Tendo abandonado o ódio, a raiva e a delusão.<br>

	Ele é aquele que eu chamo de brâmane.”</p>

