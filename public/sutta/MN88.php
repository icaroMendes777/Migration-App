<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no
Parque de Anathapindika.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. Então,
ao amanhecer, o venerável Ananda se vestiu e tomando a tigela e o manto
externo, foi para Savatthi para esmolar alimentos. Depois de haver esmolado em
Savatthi e de haver retornado, após a refeição, ele foi para o palácio da mãe
de Migara, no Parque do Oriente, para passar o resto do dia. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. Agora,
naquela ocasião o Rei Pasenadi de Kosala havia montado no elefante Ekapundarika
e estava saindo de Savatthi ao meio dia. Ele viu o venerável Ananda vindo à
distância e perguntou ao ministro Sirivaqqha: “Aquele é o venerável Ananda, não
é mesmo?” – “Sim, senhor, aquele é o venerável Ananda.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. Então o
Rei Pasenadi de Kosala disse para um homem: “Venha, bom homem, vá até o
venerável Ananda e o homenageie em meu nome com a sua cabeça aos pés dele,
dizendo: ‘Venerável senhor, o Rei Pasenadi de Kosala o homenageia com a cabeça
aos pés do venerável Ananda.’ Então diga o seguinte: ‘Venerável senhor, se o
venerável Ananda não tiver um assunto urgente a tratar, talvez o venerável
Ananda pudesse esperar um momento, por compaixão’.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. “Sim,
senhor, o homem respondeu, e ele foi até o venerável Ananda, e depois de
homenageá-lo, ele ficou em pé a um lado e disse para o venerável Ananda:
“Venerável senhor, o Rei Pasenadi de Kosala o homenageia com a cabeça aos pés
do venerável Ananda, e ele diz: ‘Venerável senhor, se o venerável Ananda não
tiver um assunto urgente a tratar, talvez o venerável Ananda pudesse esperar um
momento, por compaixão’.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. O
venerável Ananda concordou em silêncio. Então o Rei Pasenadi foi montado no
elefante até onde o elefante pôde ir e depois desmontou e foi a pé até o
venerável Ananda. Depois de cumprimentá-lo ele ficou em pé a um lado e disse
para o venerável Ananda: “Se, venerável senhor, o venerável Ananda não tiver um
assunto urgente a tratar, seria bom se ele fosse até a margem do rio Aciravati,
por compaixão.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. O
venerável Ananda concordou em silêncio. Ele foi até a margem do rio Aciravati e
sentou à sombra de uma árvore num assento que havia sido preparado. Então o Rei
Pasenadi foi montado no elefante até onde o elefante pôde ir e depois desmontou
e foi a pé até o venerável Ananda. Depois de cumprimentá-lo ele ficou em pé a
um lado e disse para o venerável Ananda: “Aqui, venerável senhor, tome este
tapete dum elefante. Que o venerável Ananda sente nele.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não é
necessário, grande rei. Sente-se. Eu estou sentado no meu próprio pano.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. O Rei
Pasenadi de Kosala sentou num assento que havia sido preparado e disse:
“Venerável Ananda, o Abençoado se comportaria com o corpo de tal forma que
pudesse ser censurado por contemplativos e brâmanes sábios?” [<a href="#N1">1</a>]
<o:p></o:p></span></p>

<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Não, grande rei, o Abençoado não se comportaria com o corpo de tal
forma que pudesse ser censurado por contemplativos e brâmanes sábios.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O
Abençoado, venerável Ananda, se comportaria com a linguagem ... se comportaria
com a mente de tal forma que pudesse ser censurado por contemplativos e
brâmanes sábios?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
grande rei, o Abençoado não se comportaria com a linguagem ...<span
style="mso-spacerun: yes">  </span>não se comportaria com a mente de tal forma
que pudesse ser censurado por contemplativos e brâmanes sábios.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. “É
maravilhoso, venerável senhor, é admirável! Pois aquilo que fomos incapazes de
lograr com uma pergunta foi logrado pelo venerável Ananda com a resposta à
pergunta. Quando pessoas tolas, não inteligentes, falam elogiando ou criticando
os outros sem fazer uma consideração cuidadosa, eu não tomo as palavras delas
com seriedade. Quanto aos expertos, os sábios e sagazes, que fazem
considerações cuidadosas antes de elogiar ou criticar, eu dou importância às
palavras deles. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. “Agora,
venerável Ananda, que tipo de ação corporal é censurada por contemplativos e
brâmanes sábios?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Qualquer ação
corporal que seja prejudicial, grande rei.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Agora,
venerável Ananda, que tipo de ação corporal é prejudicial?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Qualquer
ação corporal que seja culpável, grande rei.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Agora,
venerável Ananda, que tipo de ação corporal é culpável?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Qualquer
ação corporal que traga aflição, grande rei.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Agora,
venerável Ananda, que tipo de ação corporal traz aflição?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Qualquer
ação corporal que tenha resultados dolorosos, grande rei.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Agora,
venerável Ananda, que tipo de ação corporal tem resultados dolorosos?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Qualquer
ação corporal, grande rei, que conduza à própria aflição, ou à aflição dos
outros, ou à aflição de ambos, e por conta da qual os estados prejudiciais
aumentem e os estados benéficos diminuam. Esse tipo de ação corporal é
censurada por contemplativos e brâmanes sábios, grande rei.” [<a href="#N2">2</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>11. “Agora, venerável Ananda, que tipo de ação verbal é censurada por
contemplativos e brâmanes sábios?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Qualquer
ação verbal que seja prejudicial ... (completar como no verso 10 substituindo
“ação corporal” por “ação verbal”).<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. “Agora,
venerável Ananda, que tipo de ação mental é censurada por contemplativos e
brâmanes sábios?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Qualquer
ação mental que seja prejudicial ... (completar como no verso 10 substituindo
“ação corporal” por “ação mental”).<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. “Agora,
venerável Ananda, o Abençoado elogia apenas o abandono de todos os estados
prejudiciais?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O
Tathagata, grande rei, abandonou todos os estados prejudiciais e possui todos
os estados benéficos.” [<a href="#N3">3</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>14. “Agora, venerável Ananda, que tipo de ação corporal não é censurada
por contemplativos e brâmanes sábios?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Qualquer
ação corporal que seja benéfica, grande rei.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Agora,
venerável Ananda, que tipo de ação corporal é benéfica?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Qualquer
ação corporal que seja inculpável, grande rei.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Agora,
venerável Ananda, que tipo de ação corporal é inculpável?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Qualquer
ação corporal que não traga aflição, grande rei.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Agora,
venerável Ananda, que tipo de ação corporal não traz aflição?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Qualquer
ação corporal que tenha resultados prazerosos, grande rei.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Agora,
venerável Ananda, que tipo de ação corporal tem resultados prazerosos?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Qualquer
ação corporal, grande rei, que não conduza à própria aflição, ou à aflição dos
outros, ou à aflição de ambos, e por conta da qual os estados prejudiciais
diminuam e os estados benéficos aumentem. Esse tipo de ação corporal não é
censurada por contemplativos e brâmanes sábios, grande rei.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. “Agora,
venerável Ananda, que tipo de ação verbal não é censurada por contemplativos e
brâmanes sábios?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Qualquer
ação verbal que seja benéfica ... (completar como no verso 14 substituindo
“ação corporal” por “ação verbal”).<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16. “Agora,
venerável Ananda, que tipo de ação mental não é censurada por contemplativos e
brâmanes sábios?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Qualquer
ação mental que seja benéfica ... (completar como no verso 14 substituindo
“ação corporal” por “ação mental”).<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17. “Agora,
venerável Ananda, o Abençoado apenas elogia a adoção de todos os estados
benéficos?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O
Tathagata, grande rei, abandonou todos os estados prejudiciais e possui todos
os estados benéficos.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18. “É
maravilhoso, venerável senhor, é admirável quão bem isso foi expresso pelo
venerável Ananda! E nós estamos satisfeitos e contentes por aquilo que foi tão
bem expresso por ele. Venerável senhor, nós estamos tão satisfeitos e contentes
com aquilo que foi tão bem expresso pelo venerável Ananda que se o elefante
precioso lhe fosse permitido, nós o daríamos para ele; se o cavalo precioso lhe
fosse permitido, nós o daríamos para ele; se a bênção de um vilarejo lhe fosse
permitida, nós a daríamos para ele. Mas nós sabemos, venerável senhor, que isso
não é permitido ao venerável Ananda. Mas há esta minha capa, [<a href="#N4">4</a>]
<a name=R4></a>venerável senhor, que me foi enviada embrulhada numa caixa real
pelo Rei Ajatasattu de Magadha, com dezesseis mãos de comprimento e oito mãos
de largura. Que o venerável Ananda aceite essa capa por compaixão.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não é
necessário, grande rei. Meu manto tríplice está completo.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>19.
“Venerável senhor, este rio Aciravati já foi visto tanto pelo venerável Ananda
como por nós, depois de uma grande nuvem ter chovido pesadamente nas montanhas;
nessa ocasião, este rio Aciravati transborda em ambas as margens. Assim também,
venerável senhor, o venerável Ananda poderá fazer para si um manto tríplice
desta capa e ele poderá compartir o seu antigo manto tríplice com os seus
companheiros na vida santa. Desse modo, nossa oferta irá transbordar. Venerável
senhor, que o venerável Ananda aceite esta capa.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>20. O
venerável Ananda aceitou a capa. Então o Rei Pasenadi de Kosala disse: “E
agora, venerável senhor, nós partiremos. Estamos atarefados e temos muito que
fazer.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Agora é o
momento, grande rei, faça como julgar adequado.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, o
Rei Pasenadi de Kosala, ficando satisfeito e contente com as palavras do
venerável Ananda, levantou do seu assento e depois de homenagear o venerável
Ananda, mantendo-o à sua direita, partiu. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>21. Então,
pouco tempo depois dele haver partido, o venerável Ananda foi até o Abençoado,
e depois de cumprimentá-lo, sentou a um lado e relatou toda a conversa com o
Rei Pasenadi de Kosala, e apresentou a capa para o Abençoado. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>22. Então,
o Abençoado se dirigiu aos bhikkhus: “É um ganho, bhikkhus, para o Rei Pasenadi
de Kosala, é um grande ganho para o Rei Pasenadi de Kosala que ele tenha tido a
oportunidade de ver e homenagear o venerável Ananda.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o
que disse o Abençoado. Os bhikkhus ficaram satisfeitos e contentes com as
palavras do Abençoado.<o:p></o:p></span></p>


