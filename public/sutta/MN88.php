<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[2] Em resumo, este trecho apresenta cinco
critérios para as ações ruins: <i>prejudicial </i>enfatiza a qualidade
psicológica da ação, o seu efeito insalubre na mente; <i>censurável </i>enfatiza
a sua natureza danosa sob o ponto de vista moral; a sua capacidade de produzir <i>resultados
dolorosos </i>chama a atenção para o seu potencial cármico
indesejável; e a última afirmação chama a atenção tanto para a sua <i>motivação
</i>ruim como para as <i>conseqüências </i>prejudiciais a longo prazo que esse
tipo de ação traz tanto para si mesmo como para os outros. A explicação oposta
se aplica às ações boas, discutidas no verso 14. [<a href="#R2">Retorna</a>]<o:p></o:p></span></p>


