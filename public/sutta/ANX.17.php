<p class=Normal> "Bhikkhus, vivam com proteção, não sem proteção. Viver sem proteção é sofrimento. Há essas dez qualidades que servem como proteção. Quais dez? </p>

<p class=Normal> (1) "Aqui, um bikkhu é virtuoso. Ele permanece contido de acordo com as regras do Patimokkha, perfeito na conduta e na sua esfera de atividades. Temendo a menor falha, ele treina adotando os preceitos de virtude. Visto que o bhikkhu é virtuoso ... treina adotando os preceitos de virtude, essa qualidade serve como proteção. </p>

<p class=Normal> (2) "Outra vez, ele ouviu muito, reteve o que ouviu, memorizou o que ouviu. Todo ensinamento que é admirável no início, admirável no meio, admirável no final, com o correto significado e fraseado e que revela uma vida santa que é completamente perfeita e imaculada: esses ensinamentos ele ouviu com freqüência, memorizou, se recorda, discutiu, investigou com a sua mente e penetrou corretamente com o entendimento. Visto que o bhikkhu ouviu muito ... penetrou corretamente com o entendimento, essa qualidade também serve como proteção. </p>

<p class=Normal> (3) "Outra vez, ele tem bons amigos, companheiros e camaradas. Visto que o bhikkhu tem bons amigos, companheiros e camaradas, essa qualidade também serve como proteção. </p>

<p class=Normal> (4) "Outra vez, ele é fácil de ser corrigido e possui qualidades que fazem com que ele seja fácil de ser corrigido; ele é paciente e recebe as instruções com respeito. Visto que o bhikkhu é fácil de ser corrigido, é paciente e recebe as instruções com respeito, essa qualidade também serve como proteção. </p>

<p class=Normal> (5) "Outra vez, ele é hábil e diligente em realizar as distintas tarefas que devem ser feitas para os seus companheiros bhikkhus; ele as discerne bem de modo a planejá-las e executá-las adequadamente. Visto que o bhikkhu é hábil e diligente em realizar as distintas tarefas ... executá-las adequadamente, essa qualidade também serve como proteção. </p>

<p class=Normal> (6) "Outra vez, ele ama o Dhamma e faz afirmações que são agradáveis, com muita alegria em relação ao Dhamma e a Disciplina. Visto que o bhikkhu ama o Dhamma ...  e a Disciplina, essa qualidade também serve como proteção. </p>

<p class=Normal> (7) "Outra vez, ele permanece com a sua energia estimulada para o abandono de qualidades mentais prejudiciais e para a obtenção de qualidades mentais benéficas; ele é decidido, firme no seu esforço, sem se esquivar das suas responsabilidades com relação ao cultivo de qualidades mentais benéficas. Visto que o bhikkhu permanece com a sua energia estimulada ... cultivo de qualidades mentais benéficas, essa qualidade também serve como proteção. </p>

<p class=Normal> (8) "Outra vez, ele está satisfeito com qualquer tipo de manto, comida esmolada, moradia e medicamentos. Visto que o bhikkhu está satisfeito com qualquer tipo de manto, comida esmolada, moradia e medicamentos, essa qualidade também serve como proteção. </p>

<p class=Normal> (9) "Outra vez, ele tem atenção plena, capaz de se lembrar e se recordar daquilo que foi feito e dito faz muito tempo. Visto que o bhikkhu tem atenção plena, capaz de se lembrar e se recordar daquilo que foi feito e dito faz muito tempo, essa qualidade também serve como proteção. </p>

<p class=Normal> (10) "Outra vez, ele é sábio, ele possui a completa compreensão da origem e cessação, que é nobre e penetrante, conduzindo à completa destruição do sofrimento. Visto que o bhikkhu é sábio, ele possui a completa compreensão da origem e cessação, que é nobre e penetrante, conduzindo à completa destruição do sofrimento, essa qualidade também serve como proteção. </p>

<p class=Normal> "Bhikkhus, vivam com proteção, não sem proteção. Viver sem proteção é sofrimento. Essas são as dez qualidades que servem como proteção." </p>


