<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em certa
ocasião, o Abençoado estava em Rajagaha, no Bambual, no Santuário dos Esquilos.
Então, o errante Moliyasivaka foi até o Abençoado e depois de cumprimentá-lo
sentou a um lado e disse: <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mestre Gotama, há alguns
contemplativos e brâmanes que possuem esta doutrina e entendimento: ‘Tudo
aquilo que uma pessoa experimentar, quer seja prazer, dor, ou nem dor, nem
prazer, tudo é causado pelas ações passadas.’ Agora, o que o Mestre Gotama diz
disso?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Produzidas por distúrbios
da bílis, Sivaka, surgem certos tipos de sensações. Que isso acontece, pode ser
compreendido pela própria pessoa e também no mundo é aceito como verdadeiro.
Produzidas por distúrbios da fleuma … dos ventos… de um desequilíbrio [dos
três]… pela mudança de clima… pelo comportamento descuidado … pela violência …
pelos resultados de Kamma – por meio de tudo isso, Sivaka, surgem certos tipos
de sensações. Que isso acontece, pode ser compreendido pela própria pessoa e
também no mundo é aceito como verdadeiro. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Agora quando esses
contemplativos e brâmanes possuem esta doutrina e entendimento: ‘Tudo aquilo
que uma pessoa experimentar, quer seja prazer, dor ou nem dor, nem prazer, tudo
é causado pelas ações passadas,’ então eles vão além daquilo que conhecem por
si mesmos e daquilo que é aceito no mundo como verdadeiro. Portanto, eu digo
que esses contemplativos e brâmanes estão equivocados.” <o:p></o:p></span></p>

<p class=Normal><a name=magnif><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quan</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>do isso foi dito, Moliyasivaka disse
para o Abençoado:<span style='mso-spacerun:yes'>  </span>&quot;Magnífico,
Mestre Gotama! Magnífico, Mestre Gotama! Mestre Gotama esclareceu o Dhamma de
várias formas, como se tivesse colocado em pé o que estava de cabeça para
baixo, revelasse o que estava escondido, mostrasse o caminho para alguém que
estivesse perdido ou segurasse uma lâmpada no escuro para aqueles que possuem
visão pudessem ver as formas. Nós buscamos refúgio no Mestre Gotama, no Dhamma
e na Sangha dos bhikkhus. Que o Mestre Gotama me aceite como discípulo leigo
que buscou refúgio para o resto da vida.”<o:p></o:p></span></p>

