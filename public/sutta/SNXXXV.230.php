<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
suponham que um pescador lançasse um anzol com uma isca num lago profundo e um
peixe em busca de comida engolisse o anzol com a isca. Aquele peixe, ao engolir
o anzol do pescador deu de encontro com a calamidade e o desastre e o pescador
poderá fazer o que quiser com ele. Do mesmo modo, bhikkhus, há esses seis
anzóis no mundo<span style="mso-spacerun: yes">  </span>para a calamidade e
desastre dos seres, para o massacre dos seres vivos. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Há,
bhikkhus, formas conscientizadas através do olho que são desejáveis, agradáveis e
fáceis de serem gostadas, conectadas com o desejo sensual e que provocam a
cobiça. Se um bhikkhu sente prazer com elas, acolhe-as e permanece atado a
elas, ele é chamado um bhikkhu que engoliu o anzol de Mara. Ele deu de encontro
com a calamidade e o desastre e o Senhor do Mal poderá fazer o que quiser com
ele.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Há,
bhikkhus, sons conscientizados pelo ouvido ... objetos mentais conscientizados pela mente
que são desejáveis ... provocam a cobiça. Se um bhikkhu sente prazer com eles
... o Senhor do Mal poderá fazer o que quiser com ele.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Há,
bhikkhus, formas conscientizadas através do olho que são desejáveis, agradáveis e
fáceis de serem gostadas, conectadas com o desejo sensual e que provocam a
cobiça. Se um bhikkhu não sente prazer com elas, não as acolhe e não permanece
atado a elas, ele é chamado um bhikkhu que não engoliu o anzol de Mara, que
quebrou o anzol, destruiu o anzol. Ele não deu de encontro com a calamidade e o
desastre e o Senhor do Mal não poderá fazer o que quiser com ele.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Há,
