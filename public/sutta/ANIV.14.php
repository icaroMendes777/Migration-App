<p class=Normal>"Bhikkhus, há esses quatro esforços. Quais quatro? O esforço através da contenção, o esforço através do abandono, o esforço através da meditação, e o esforço através da preservação. </p>

<p class=Normal> (1) "E como, bhikkhus, é o esforço através da contenção? Neste caso, ao ver uma forma com o olho, um bhikkhu não se agarra aos seus sinais ou detalhes. Visto que, se permanecer com a faculdade do olho descuidada, ele será tomado pelos estados ruins e prejudiciais de cobiça e tristeza. Ele pratica a contenção, ele protege a faculdade do olho, ele se empenha na contenção da faculdade do olho. Tendo ouvido um som com o ouvido ... Tendo cheirado um aroma com o nariz ... Tendo saboreado um sabor com a língua ... Tendo tocado um tangível com o corpo ... Tendo conscientizado um objeto mental com a mente, um bhikkhu não se agarra aos seus sinais ou detalhes. Visto que, se permanecer com a faculdade da mente descuidada, ele será tomado pelos estados ruins e prejudiciais de cobiça e tristeza. Ele pratica a contenção, ele protege a faculdade da mente, ele se empenha na contenção da faculdade da mente. Esse é chamado o esforço através da contenção. </p>

<p class=Normal> (2) "E como é o esforço através do abandono? Neste caso, um bhikkhu não aceita um pensamento de desejo sensual que tenha surgido, mas o abandona, dissipa, destrói, faz com que desapareça. Ele não aceita um pensamento de má vontade que tenha surgido, mas o abandona, dissipa, destrói, faz com que desapareça. Ele não aceita um pensamento de crueldade que tenha surgido, mas o abandona, dissipa, destrói, faz com que desapareça. Esse é chamado o esforço através do abandono. </p>

<p class=Normal> (3) "E como é o esforço através da meditação? Neste caso, um bhikkhu desenvolve o fator da iluminação da atenção plena, que tem como base o afastamento, desapego e cessação que amadurece no abandono. Ele desenvolve o fator da iluminação da investigação dos fenômenos ... Ele desenvolve o fator da iluminação da energia ... Ele desenvolve o fator da iluminação do êxtase ... Ele desenvolve o fator da iluminação da tranqüilidade ... Ele desenvolve o fator da iluminação da concentração ... Ele desenvolve o fator da iluminação da equanimidade, que tem como base o afastamento, desapego e cessação que amadurece no abandono. Esse é chamado o esforço através da meditação. </p>

<p class=Normal> (4) "E como é o esforço através da preservação? Neste caso, um bhikkhu mantém com firmeza na sua mente um objeto de concentração favorável que tenha surgido,  como a percepção de um esqueleto, ou a percepção de um cadáver cheio de vermes, ou a percepção de um cadáver lívido, ou a percepção de um cadáver supurando, ou a percepção de um cadáver fissurado, ou a percepção de um cadáver inchado. Esse é chamado o esforço através da preservação. </p>

<p class=Normal>"Esses bhikkhus são os quatro esforços." </p>

<p class=Normal>Contenção e abandono,<br>
meditação e preservação: <br>
esses quatro tipos de esforço foram ensinados <br>
pelo parente do Sol. <br>
Por esses meios, um bhikkhu ardente, pode aqui<br>
realizar a destruição do sofrimento. </p>

