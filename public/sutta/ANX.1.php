<p class=Tit3 align=center style='text-align:center'><b>Anguttara Nikaya X.1 </b></Tit3>
<p class=Tit1 align=center style='text-align:center'> Kimatthiya Sutta </Tit1>
<p class=Tit1 align=center style='text-align:center'> Qual o Propósito </Tit1>

<p class=Normal align=center style='text-align:center'><span lang=PT-BR style='font-size:
7.0pt;mso-ansi-language:PT-BR'><b> Somente para distribuição gratuita.</b><br>
