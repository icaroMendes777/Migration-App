<p class=Normal><b>Notas:</b></p>

<p class=Normal>Os equivalentes em Pali para alguns dos termos:<br>
virtude - <i>sila</i><br>
arrependimento, remorso - <i>vippatisara</i><br>
satisfação, contentamento - <i>pamujja</i><br>
êxtase - <i>piti</i><br>
calma, tranquilidade, serenidade - <i>passaddhi</i><br>
felicidade - <i>sukha</i><br>
concentração - <i>samadhi</i><br>
ver as coisas como elas na verdade são - <i>yatha butha ñanadassana</i><br>
desencantamento - <i>nibbida</i><br>
desapego -<i> viraga</i> </p>




