<p class=Normal style='margin-left:.75in'> 


<p class=Normal>Assim ouvi. Em certa ocasião o
Abençoado estava em  Savatthi, no Bosque de Jeta, no Parque de Anathapindika.
Então o Venerável Ananda foi
até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse: </p>

<p class=Normal>&quot;Qual é o
propósito das virtudes hábeis? Qual é a sua recompensa?&quot; <o:p></o:p></span></p>




<p class=Normal> 
&quot;As virtudes hábeis têm a libertação do arrependimento como seu propósito,
Ananda, e a libertação do arrependimento como sua recompensa.&quot;<span
style="mso-spacerun: yes">  </span><o:p></o:p></span></p>




<p class=Normal>
&quot;E qual é o propósito da libertação do arrependimento? Qual é a sua
recompensa?&quot; <o:p></o:p></span></p>




<p class=Normal>
&quot;Libertação do arrependimento tem a satisfação como seu propósito,
satisfação como sua recompensa.&quot; <o:p></o:p></span></p>




<p class=Normal>
&quot;E qual é o propósito da satisfação? Qual é a sua
recompensa?&quot; <o:p></o:p></span></p>




<p class=Normal>
&quot;Satisfação tem o êxtase como seu propósito, êxtase como
recompensa&quot; <o:p></o:p></span></p>




<p class=Normal>
&quot;E qual é o propósito do êxtase? Qual é a sua recompensa?&quot; <o:p></o:p></span></p>




<p class=Normal>
&quot;Êxtase tem a calma como seu propósito, calma como
sua recompensa.&quot; <o:p></o:p></span></p>




<p class=Normal>
&quot;E qual é o propósito da calma? Qual é a sua
recompensa?&quot; <o:p></o:p></span></p>




<p class=Normal>
&quot;Calma tem a felicidade como seu propósito, felicidade
como sua recompensa.&quot; <o:p></o:p></span></p>




<p class=Normal>
&quot;E qual é o propósito da felicidade? Qual é a sua
recompensa?&quot; <o:p></o:p></span></p>




<p class=Normal>
&quot;Felicidade tem a concentração como seu propósito, concentração
como sua recompensa.&quot; <o:p></o:p></span></p>




<p class=Normal>
&quot;E qual é o propósito da concentração? Qual é a sua
recompensa?&quot; <o:p></o:p></span></p>




<p class=Normal>
&quot;Concentração tem conhecimento e visão das coisas como elas na
verdade são como seu propósito, conhecimento e visão das coisas como elas na
verdade são como sua recompensa.&quot; <o:p></o:p></span></p>




<p class=Normal>
&quot;E qual é o propósito de conhecimento e visão das coisas como elas
na verdade são? Qual é a sua recompensa?&quot; <o:p></o:p></span></p>




<p class=Normal>
&quot;Conhecimento e visão das coisas como elas na verdade são tem
desencantamento e desapego como seu propósito, desencantamento como sua recompensa.&quot; <o:p></o:p></span></p>




<p class=Normal>
&quot;E qual é o propósito de desencantamento e desapego? Qual é a sua
recompensa?&quot; <o:p></o:p></span></p>









<p class=Normal>
&quot;Desencantamento e desapego têm conhecimento e visão da libertação como seu
propósito, conhecimento e visão da libertação como sua recompensa. <o:p></o:p></span></p>




<p class=Normal>
&quot;Assim dessa forma, Ananda, virtudes hábeis têm libertação do arrependimento como seu propósito, libertação do arrependimento como sua recompensa. Libertação
do arrependimento tem a satisfação como seu propósito, satisfação como sua
recompensa. Satisfação tem êxtase como seu propósito, êxtase como sua recompensa.
Êxtase tem tranquilidade como seu propósito, tranquilidade como sua recompensa.
Tranquilidade tem a felicidade como seu propósito, felicidade como sua
recompensa. Felicidade tem a concentração como seu propósito, concentração como
sua recompensa. Concentração tem conhecimento e visão das coisas como elas na
verdade são como seu propósito, conhecimento e visão das coisas como elas na
verdade são como sua recompensa. Conhecimento e visão das coisas como elas na
verdade são tem desencantamento e desapego como seu propósito, desencantamento e desapego como sua recompensa.   Desencantamento e desapego têm conhecimento e visão da libertação como seu propósito,
conhecimento e visão da libertação como sua recompensa.<o:p></o:p></span></p>

<p class=Normal>
&quot;Dessa forma, Ananda, virtudes hábeis conduzem passo a passo à
consumação do estado de arahant.&quot; <o:p></o:p></span></p>

<p class=Normal>&nbsp;</p>
