<p class=Normal>"Bhikkhus, certa ocasião, eu estava em Uruvela às margens do rio Neranjara ao pé da figueira-dos-pagodes, pouco tempo depois de ter alcançado a perfeita iluminação. Então, um número de brâmanes velhos, com a idade avançada, atribulados pelos anos, avançados na vida, tendo chegado ao último estágio, vieram até mim e depois da troca de saudações corteses e amáveis, sentaram a um lado e disseram: </p>

<p class=Normal>"Nós ouvimos mestre Gotama: 'O contemplativo Gotama não presta homenagem aos brâmanes que são velhos, com a idade avançada, atribulados pelos anos, avançados na vida, já no último estágio, nem que ele se levanta ou lhes oferece um assento.' Isto de fato é verdade, pois o mestre Gotama não presta homenagem aos brâmanes que são velhos, com a idade avançada, atribulados pelos anos, avançados na vida, já no último estágio, nem que ele se levanta ou lhes oferece um assento. Isso não é bom, mestre Gotama." </p>

<p class=Normal>"Então o pensamento me ocorreu: Esses veneráveis não sabem o que é um ancião ou quais são as qualidades que caracterizam um ancião. Muito embora alguém seja velho - oitenta, noventa, ou cem anos - se ele falar na hora imprópria, disser mentiras, falar o que não traz benefício, falar em contrário ao Dhamma e a Disciplina, se no momento inapropriado ele disser palavras que são inúteis, irracionais, dispersas, que não trazem benefício, então, ele é reconhecido como um ancião tolo [infantil]. </p>

<p class=Normal>"Mas mesmo que alguém seja jovem, com o cabelo negro, dotado com as bênçãos da juventude, no primeiro estágio da vida, se ele falar na hora apropriada, disser a verdade, falar o que traz benefício, falar de acordo com Dhamma e a Disciplina, se no momento apropriado ele disser palavras que são dignas de serem guardadas, racionais, sucintas, que trazem benefício, então, ele é reconhecido como um ancião sábio. </p>

<p class=Normal>"Bhikkhus, há essas quatro qualidades que caracterizam um ancião. Quais quatro? </p>

<p class=Normal> (1) "Aqui, um bhikkhu é virtuoso. Ele permanece contido de acordo com o Patimokkha, perfeito na conduta e na sua esfera de atividades. Temendo a menor falha, ele treina adotando os preceitos de virtude. </p>

<p class=Normal> (2) "Ele aprendeu muito, se recorda daquilo que aprendeu e consolida aquilo que aprendeu. Aqueles ensinamentos que são admiráveis no início, admiráveis no meio, admiráveis no final, com o correto significado e fraseado e que revelam uma vida santa que é completamente perfeita e imaculada – ensinamentos como esses ele os aprendeu bem, se recorda, domina com a linguagem, investigou com a mente e penetrou corretamente com o entendimento. </p>

<p class=Normal> (3) "Ele obtém de acordo com a sua vontade, sem problemas ou dificuldades, os quatro jhanas que constituem a mente superior e que proporcionam uma estada prazerosa aqui e agora. </p>

<p class=Normal> (4) "Com a eliminação das impurezas mentais, ele permanece em um estado livre de impurezas com a libertação da mente e a libertação através da sabedoria, tendo conhecido e manifestado isso para si mesmo no aqui e agora. </p>

<p class=Normal>"Essas são as quatro qualidades que caracterizam um ancião." </p>

<p class=Normal>O tolo com a mente inquieta<br>
que tagarela muito, <br>
os pensamentos inconstantes, <br>
que se delicia com os ensinamentos ruins, <br>
com idéias ruins, desrespeitoso, <br>
está longe da estatura de um ancião. </p>

<p class=Normal>Mas aquele consumado em virtude, <br>
com aprendizado e discernimento, <br>
com autocontrole nos fatores que dão estabilidade, [<a href="#N1">1</a>]<br>
que vê o significado com clareza por meio da sabedoria; <br>
tendo superado todos os fenômenos, <br>
não estéril, com discernimento, <br>
tendo abandonado o nascimento e morte, <br>
perfeito na vida espiritual, <br>
nele não há impurezas -<br>
ele é quem chamo ancião. <br>
Com a destruição das impurezas<br>
um bhikkhu é chamado ancião. </p>

