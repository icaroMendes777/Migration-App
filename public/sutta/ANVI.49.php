<p class=Normal>Certa ocasião o Abençoado estava em Savatthi, no Bosque de Jeta,  no Parque de Anathapindika. E naquela ocasião o Venerável Khema e o Venerável Sumana estavam em  Savatthi no Bosque do Cego. Então o Venerável Khema e o Venerável Sumana foram até o Abençoado e depois de cumprimentá-lo sentaram a um lado e o Venerável Khema disse para o Abençoado, “Quando um bhikkhu é um arahant, que destruiu as impurezas, viveu a vida santa, fez o que devia ser feito, depôs o fardo, alcançou o verdadeiro objetivo, destruiu os grilhões da existência e está completamente libertado através do conhecimento supremo – o pensamento não lhe ocorre que ‘Há alguém melhor do que eu,’ ou que ‘Há alguém igual a mim,’ ou que ‘Há alguém pior do que eu.’” Isso foi o que disse o Venerável Khema, e o Mestre aprovou. Percebendo que “O Mestre me aprovou,” o Venerável Khema levantou do seu assento e depois de homenagear o Abençoado, mantendo-o à sua direita, partiu. </p> 

<p class=Normal>Então o Venerável Sumana, não muito tempo depois do Venerável Khema ter partido, disse para o Abençoado, “Quando um bhikkhu é um arahant, que destruiu as impurezas, viveu a vida santa, fez o que devia ser feito, depôs o fardo, alcançou o verdadeiro objetivo, destruiu os grilhões da existência e está completamente libertado através do conhecimento supremo – o pensamento não lhe ocorre que ‘Não há ninguém melhor do que eu,’ ou que ‘Não há ninguém igual a mim,’ ou que ‘Não há ninguém pior do que eu.’” Isso foi o que disse o Venerável Sumana, e o Mestre aprovou. Percebendo que “O Mestre me aprovou,” o Venerável Sumana levantou do seu assento e depois de homenagear o Abençoado, mantendo-o à sua direita, partiu. </p>

<p class=Normal>Então o Abençoado, não muito tempo depois do Venerável Khema e do Venerável Sumana terem partido, disse para os bhikkhus, “Bhikkhus, assim é como os membros de um clã declaram o conhecimento supremo. O significado [do conhecimento] é dito, mas sem a alusão a um eu. No entanto há algumas pessoas tolas que declaram o conhecimento supremo como se fosse por brincadeira. Mas depois elas enfrentarão problemas por isso.” </p>

<p class=Normal style='margin-left:.75in'>Não como superior, inferior, ou igual,<br>
eles referem a si mesmos.<br>
Com o nascimento destruído,<br>
livre dos grilhões,<br>
eles vivem a vida santa.” </p>

