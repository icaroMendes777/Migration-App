<p class=Normal> “Bhikkhus, quanto aos fatores externos, eu não vejo nenhum outro fator que contribua tanto para o surgimento dos sete fatores da iluminação quanto este: ter pessoas admiráveis como bons amigos. Quando um bhikkhu tem pessoas admiráveis como bons amigos, é de se esperar que ele desenvolva e cultive os sete fatores da iluminação.</p> 
 
<p class=Normal> “E como um bhikkhu que tem pessoas admiráveis como bons amigos desenvolve e cultiva os sete fatores da iluminação? Nesse caso, bhikkhus, um bhikkhu desenvolve o fator da iluminação da atenção plena, que tem como base o afastamento, desapego e cessação,  que amadurece no abandono ... ele desenvolve o fator da iluminação da equanimidade, que tem como base o afastamento, desapego e cessação,  que amadurece no abandono. É dessa forma, bhikkhus, que um bhikkhu que tem pessoas admiráveis como bons amigos desenvolve e cultiva os sete fatores da iluminação.”</p>

