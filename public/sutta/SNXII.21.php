<p class=Normal> Em Savatthi. “Bhikkhus, possuindo esses dez poderes e esses quatro tipos de intrepidez o Tathagata reivindica o lugar de líder do rebanho, ruge o seu rugido de leão nas assembléias e coloca em movimento a roda de Brahma: ‘Assim é a forma, essa é a sua origem, essa é a sua cessação; assim é a sensação, essa é a sua origem, essa é a sua cessação; assim é a percepção, essa é a sua origem, essa é a sua cessação; assim são as formações volitivas, essa é a sua origem, essa é a sua cessação; assim é a consciência, essa é a sua origem, essa é a sua cessação. Portanto, quando existe isso, aquilo existe; com o surgimento disso, aquilo surge. Quando não existe isso, aquilo também não existe; com a cessação disto, aquilo cessa. Isto é, da ignorância como condição, as formações volitivas [surgem]. Das formações volitivas como condição, a consciência. Da consciência como condição, a mentalidade-materialidade (nome e forma). Da mentalidade-materialidade (nome e forma) como condição, as seis bases dos sentidos. Das seis bases dos sentidos como condição, o contato. Do contato como condição, a sensação. Da sensação como condição, o desejo. Do desejo como condição, o apego. Do apego como condição, o ser/existir. Do ser/existir como condição, o nascimento. Do nascimento como condição, então o envelhecimento e morte, tristeza, lamentação, dor, angústia e desespero surgem. Essa é a origem de toda essa massa de sofrimento. Mas do desaparecimento e cessação sem deixar vestígios dessa mesma ignorância cessam as formações volitivas. Da cessação das formações volitivas cessa a consciência. Da cessação da consciência cessa a mentalidade-materialidade (nome e forma). Da cessação da mentalidade-materialidade (nome e forma) cessam as seis bases dos sentidos. Da cessação das seis bases dos sentidos cessa o contato. Da cessação do contato cessa a sensação. Da cessação da sensação cessa o desejo. Da cessação do desejo cessa o apego. Da cessação do apego cessa o ser/existir. Da cessação do ser/existir cessa o nascimento. Da cessação do nascimento, então o envelhecimento e morte, tristeza, lamentação, dor, angústia e desespero, tudo cessa. Essa é a cessação de toda essa massa de sofrimento.”</p>

