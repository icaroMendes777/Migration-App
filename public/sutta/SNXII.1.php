<p class=Normal>Assim ouvi. Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no Parque de Anathapindika. Lá ele se dirigiu aos monges desta forma: “Bhikkhus” – “Venerável Senhor,” eles responderam. O Abençoado disse o seguinte: </p>

<p class=Normal>"Bhikkhus, eu lhes ensinarei a origem dependente. Ouçam e prestem muita atenção àquilo que eu vou dizer.” – “Sim, venerável senhor,” os bhikkhus responderam. O Abençoado disse o seguinte: </p>

<p class=Normal>Da ignorância como condição, as formações volitivas [surgem].<br>
Das formações volitivas como condição, a consciência. <br>
Da consciência como condição, a mentalidade-materialidade (nome e forma). <br>
Da mentalidade-materialidade (nome e forma) como condição, as seis bases dos sentidos. <br>
Das seis bases dos sentidos como condição, o contato. <br>
Do contato como condição, a sensação. <br>
Da sensação como condição, o desejo. <br>
Do desejo como condição, o apego. <br>
Do apego como condição, o ser/existir. <br>
Do ser/existir como condição, o nascimento. <br>
Do nascimento como condição, então o envelhecimento e morte, tristeza, lamentação, dor, angústia e desespero surgem. Essa é a origem de toda essa massa de sofrimento. </p>

<p class=Normal>Isto bhikkhus é chamado origem dependente. </p>

<p class=Normal>"Agora, do desaparecimento e cessação sem deixar vestígios dessa mesma ignorância cessam as formações volitivas. Da cessação das formações volitivas cessa a consciência. Da cessação da consciência cessa a mentalidade-materialidade (nome e forma). Da cessação da mentalidade-materialidade (nome e forma) cessam as seis bases dos sentidos. Da cessação das seis bases dos sentidos cessa o contato. Da cessação do contato cessa a sensação. Da cessação da sensação cessa o desejo. Da cessação do desejo cessa o apego. Da cessação do apego cessa o ser/existir. Da cessação do ser/existir cessa o nascimento. Da cessação do nascimento, então envelhecimento e morte, tristeza, lamentação, dor, angústia e desespero, tudo cessa. Essa é a cessação de toda essa massa de sofrimento." </p>

<p class=Normal>Isso foi o que o Abençoado disse, os bhikkhus ficaram contentes e satisfeitos com as palavras do Abençoado. </p>

