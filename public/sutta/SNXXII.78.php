<p class=Normal> “Bhikkhus, o leão, o rei dos animais, ao anoitecer sai do seu covil. Ele se espreguiça, inspeciona os quatro quadrantes, ruge o seu rugido de leão três vezes e depois ele vai em busca da caça. </p>

<p class=Normal> “Agora, quaisquer animais que ouçam o rugido do leão, a maioria são agarrados pelo medo, agitação e terror. Aqueles animais que vivem nos buracos, se escondem nos buracos; aqueles que vivem nas florestas se refugiam nas florestas; e os pássaros se elevam no céu. Todos os elefantes reais que vivem nos vilarejos, cidades ou capitais, amarrados com fortes correias de couro, arrebentam e rompem essas correias e passando urina e excremento, correm em todas as direções apavorados. Tanto poder, bhikkhus, tem o leão, o rei dos animais, sobre os outros animais, tão poderosa é a sua influência e majestade. </p>

<p class=Normal> “De modo semelhante, bhikkhus, um Tathagata surge no mundo, um arahant, perfeitamente iluminado, consumado no verdadeiro conhecimento e conduta, bem-aventurado, conhecedor dos mundos, um líder insuperável de pessoas preparadas para serem treinadas, mestre de devas e humanos, desperto, sublime. Ele ensina o Dhamma assim: ‘Assim é a forma, essa é a origem da forma, essa é a cessação da forma; essa é a sensação ... a percepção ... as formações volitivas ... a consciência, essa é a origem da consciência, essa é a cessação da consciência.'</p>

<p class=Normal> “Então, bhikkhus, quaisquer devas que existam – com vida longa, belos, cheios de felicidade, vivendo por muito tempo nos seus paraísos celestiais – eles também, ao ouvirem o Dhamma ensinado pelo Tathagata, a maioria são agarrados pelo medo, agitação e terror, e exclamam: ‘Oh, nós que pensávamos sermos permanentes somos na verdade impermanentes. Nós que pensávamos estarmos seguros estamos na verdade inseguros. Nós que pensávamos sermos eternos somos na verdade não-eternos. Portanto de fato somos impermanentes, inseguros e não-eternos e estamos dentro da esfera da identidade.’” </p>

<p class=Normal> "Tanto poder, bhikkhus, tem o Tathagata sobre o mundo com os seus devas, tão poderosa é a sua influência e majestade." </p>

<p class=Normal> Isso foi o que o Abençoado disse. Tendo dito isso o Mestre disse ainda mais: </p>

<p class=Normal> "Quando o Buda, através do conhecimento direto, <br>
coloca em movimento a roda do Dhamma, <br>
o Mestre insuperável no mundo<br>
com os seus devas [torna isto conhecido]: </p>

<p class=Normal> "A cessação da identidade<br>
e a origem da identidade, <br>
também o Nobre Caminho Óctuplo<br>
que conduz ao alívio do sofrimento. </p>

<p class=Normal> "Então aqueles devas com vida longa, <br>
belos, resplandecentes na sua glória, <br>
são tomados pelo medo e terror, <br>
tal como os animais que ouvem o rugido do leão. </p>

<p class=Normal> "'Nós não transcendemos a identidade; <br>
pelo que parece somos impermanentes.'<br>
[Assim eles dizem] tendo ouvido os ensinamentos<br>
do Arahant, o Liberto." </p>


