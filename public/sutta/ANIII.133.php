<p class=Normal> 
&quot;Bhikkhus, existem três tipos de indivíduos que podem ser
encontrados no mundo. Quais três? Um indivíduo como uma inscrição na rocha, um indivíduo
como uma inscrição no solo e, um indivíduo como uma inscrição na água.<o:p></o:p></span></p>


<p class=Normal>
“ E como é um indivíduo como uma inscriç­ão na rocha? É o caso em que
um indivíduo está freqüentemente enraivecido e a raiva permanece com ele por um
longo tempo. Tal como a inscrição na rocha não é apagada rapidamente pelo vento
ou pela água e dura um longo tempo, da mesma forma um indivíduo está
freqüentemente enraivecido e a raiva permanece com ele por um longo tempo. Este
é um indivíduo a quem se denomina como uma inscrição em uma rocha.<o:p></o:p></span></p>


<p class=Normal>
“ E como é um indivíduo como uma inscriç­ão no solo? É o caso em que um
indivíduo está freqüentemente enraivecido porém a raiva não permanece com ele
por um longo tempo. Tal como a inscrição no solo é apagada rapidamente pelo
vento ou pela água e não dura um longo tempo, da mesma forma um indivíduo
está freqüentemente enraivecido porém a raiva não permanece com ele por um
longo tempo. Este é um indivíduo a quem se denomina como uma inscrição no solo.<o:p></o:p></span></p>


<p class=Normal>
“ E como é um indivíduo como uma inscrição na água? É o caso em que um
indivíduo – quando se lhe dirige a palavra de maneira rude, de maneira
grosseira, de maneira desagradável – se mantém a despeito disso agradável,
sociável e cortês. Tal como uma inscrição na água que desaparece imediatamente
e não dura um longo tempo, da mesma forma um indivíduo – quando se lhe
dirige a palavra de maneira rude, de maneira grosseira, de maneira desagradável
– se mantém a despeito disso agradável, sociável e cortês. Este é um indivíduo
a quem se denomina como uma inscrição na água.<o:p></o:p></span></p>

