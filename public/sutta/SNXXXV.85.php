<p class=Normal> “Ananda, porque está vazio de um eu ou algo que pertença a um eu é que
dizem, ‘O mundo está vazio.’ E o que é que está vazio de um eu ou algo que
pertença a um eu? O olho, Ananda, está vazio de um eu ou algo que pertença a um
eu, as formas ... a consciência no olho ... o contato no olho ... qualquer sensação que
surja tendo o contato no olho como condição - experimentada como prazer, dor
ou nem prazer, nem dor - está vazia de um eu ou algo que pertença a um eu. O ouvido está vazio de um eu ou algo que pertença a um
eu ... A mente está vazia de um eu ou algo que pertença a um
eu, os objetos mentais ... a consciência na mente ... o contato na mente ... qualquer sensação que
surja tendo o contato na mente como condição - experimentada como prazer, dor
ou nem prazer, nem dor - está vazia de um eu ou algo que pertença a um eu.<o:p></o:p></span></p>




<p class=Normal> “Ananda, é porque está vazio de um eu ou algo que pertença a um eu que
