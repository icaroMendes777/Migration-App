<p class=Normal> Em Savatthi. “Bhikkhus, antes
da minha iluminação, quando eu ainda era apenas um Bodisatva não iluminado,
compreendi o seguinte: ‘Quanta dificuldade existe neste mundo! O nascimento, o
envelhecimento, a morte, o falecimento e o renascimento, porém não há a
completa compreensão de como escapar<span style="mso-spacerun: yes"> 
</span>desse sofrimento, do envelhecimento e da morte.<span
style="mso-spacerun: yes">  </span>Quando haverá a completa compreensão de como
escapar desse sofrimento, do envelhecimento e da morte?’<o:p></o:p></span></p>


<p class=Normal> “Então<span style="mso-spacerun: yes">  </span>me ocorreram os seguintes
pensamentos:</p> 

<p class=Normal>‘Envelhecimento e morte surgem quando o que mais surge? Sob qual
condição surge o envelhecimento e a morte ?’ Aplicando a atenção com sabedoria
compreendi: ‘Envelhecimento e morte surgem quando o nascimento surge. Do
nascimento como condição surgem o envelhecimento e a morte.’</p>
 
<p class=Normal>‘Nascimento surge quando o que mais surge? Sob qual condição surge o
nascimento?’ Aplicando a atenção com sabedoria compreendi: ‘Nascimento surge
quando<span style="mso-spacerun: yes">  </span>surge o ser/existir. Do
ser/existir<span style="mso-spacerun: yes">  </span>como condição surge o
nascimento.’ </p> 

<p class=Normal>‘O ser/existir surge quando o que mais surge? Sob qual
condição surge o ser/existir?’ Aplicando a atenção com sabedoria compreendi:
‘O ser/existir surge quando<span style="mso-spacerun: yes">  </span>surge a
mentalidade-materialidade (nome e forma). Da mentalidade-materialidade (nome e forma) como condição surge o
ser/existir.’ </p> 

<p class=Normal>‘A mentalidade-materialidade (nome e forma) surge quando o que mais
surge? Sob qual condição surge a mentalidade-materialidade (nome e forma)?’ Aplicando a
atenção com sabedoria compreendi: ‘A mentalidade-materialidade (nome e forma) surge quando
surge a consciência. Da consciência como condição surge a
mentalidade-materialidade (nome e forma).’ </p> 

<p class=Normal>‘A consciência surge quando o que mais surge?
Sob qual condição surge a consciência?’ Aplicando a atenção com sabedoria
compreendi: ‘A consciência surge quando<span style="mso-spacerun: yes"> 
</span>surge a mentalidade-materialidade (nome e forma). Da mentalidade-materialidade (nome e forma) como
condição surge a consciência.’<o:p></o:p></span></p>


<p class=Normal> “Então me ocorreu o seguinte pensamento, ‘A consciência regressa até a
mentalidade-materialidade (nome e forma) e não vai mais além. Essa é a extensão do nascimento,
envelhecimento, morte, falecimento e renascimento, isto é, da
mentalidade-materialidade (nome e forma) como condição surge a consciência, da consciência
como condição surge a mentalidade-materialidade (nome e forma), da mentalidade-materialidade (nome e forma)
como condição surgem as seis bases dos sentidos ... contato ... sensação ... 
desejo ... apego ... ser/existir ... nascimento ... envelhecimento, morte, tristeza, lamentação,
dor, angústia e desespero. Essa é a origem de toda essa
massa de sofrimento. Originação, originação. Surgiu a visão, surgiu o conhecimento,
surgiu a sabedoria,<span style="mso-spacerun: yes">  </span>surgiu o
conhecimento verdadeiro, surgiu a iluminação em relação a coisas nunca antes
ouvidas.<o:p></o:p></span></p>


<p class=Normal> “Então, me ocorreram os seguintes pensamentos:</p> 

<p class=Normal>‘Envelhecimento e morte não
surge quando o que mais não surge? O que cessando, cessará o envelhecimento
e a morte ?’ Aplicando a atenção com sabedoria compreendi: ‘Envelhecimento e
morte não surge quando o nascimento não surge. Cessando o nascimento cessa
o envelhecimento e a morte.’</p> 

<p class=Normal>‘Nascimento não
surge ... ser/existir  ... apego ... desejo ... sensação ... contato ... seis
bases dos sentidos ... Cessando a mentalidade-materialidade (nome e forma) cessam as
seis
bases dos sentidos’.</p>

<p class=Normal>‘Mentalidade-materialidade (nome e forma) não surge quando o que
mais não surge? O que cessando, cessa a mentalidade-materialidade (nome e forma) ?’
Aplicando a atenção com sabedoria compreendi: ‘Mentalidade-materialidade (nome e forma) não surge
quando a consciência não surge. Cessando a consciência cessa a
mentalidade-materialidade (nome e forma).’</p> 
<p class=Normal>‘Consciência<span style="mso-spacerun: yes">  </span>não surge quando o que
mais não surge? O que cessando, cessa a consciência?’ Aplicando a atenção com
sabedoria compreendi: ‘Consciência não surge quando a
mentalidade-materialidade (nome e forma) não surge. Cessando a mentalidade-materialidade (nome e forma)
cessa a consciência.’<o:p></o:p></span></p>


<p class=Normal> “Então<span style="mso-spacerun: yes">  </span>me ocorreu o seguinte
pensamento, ‘Eu descobri esse caminho para a iluminação, isto é, cessando a
mentalidade-materialidade (nome e forma) cessa a consciência, cessando a consciência cessa a
mentalidade-materialidade (nome e forma). Cessando a mentalidade-materialidade (nome e forma), cessam as seis
bases dos sentidos. Cessando as seis bases dos sentidos, cessa o contato.
Cessando o contato, cessa a sensação. Cessando a sensação, cessa o desejo.
Cessando o desejo, cessa o apego. Cessando o apego, cessa o ser/existir.
Cessando o ser/existir, cessa o nascimento. Cessando o nascimento,<span
style="mso-spacerun: yes">  </span>envelhecimento, morte, tristeza, lamentação,
dor, angústia e desespero, tudo cessa. Essa é a cessação de toda essa massa de
sofrimento. Cessação, cessação. Surgiu a visão, surgiu o conhecimento, surgiu a
sabedoria,<span style="mso-spacerun: yes">  </span>surgiu o conhecimento
verdadeiro, surgiu a iluminação em relação a coisas nunca antes ouvidas.<o:p></o:p></span></p>


<p class=Normal> “É como se um homem, caminhando por uma trilha na floresta, se
deparasse com um caminho antigo, uma estrada antiga, utilizada por pessoas em
tempos passados. Ele a seguiria. Seguindo-a ele encontraria uma antiga cidade,
uma antiga capital habitada por pessoas em tempos passados, completa com
parques, bosques e lagos, murada, encantadora. Ele se dirigiria ao rei ou ao
seu ministro dizendo, ‘Senhor, você deveria saber que caminhando por uma trilha
na floresta eu vi um caminho antigo …. Eu o segui ... eu vi uma antiga cidade,
uma antiga capital ... completa com parques, bosques e lagos, murada,
encantadora. Senhor, reconstrua essa cidade!’ O rei ou o seu ministro
reconstruiria a cidade de modo que ela se tornasse rica, próspera e populosa,
repleta de gente e bem suprida.<o:p></o:p></span></p>


<p class=Normal> “Da mesma forma, eu vi um antigo caminho, uma antiga estrada, trilhada
pelos iluminados de tempos passados. E qual é esse antigo caminho, essa antiga
estrada, trilhada pelos iluminados de tempos passados? É este nobre caminho
óctuplo: entendimento correto, pensamento correto, linguagem correta, ação
correta, modo de vida correto, esforço correto, atenção plena correta,
concentração correta. Esse é o antigo caminho, essa é a antiga estrada,
trilhada pelos iluminados de tempos passados. Eu segui esse caminho.
Seguindo-o, cheguei ao conhecimento direto do envelhecimento e da morte,
conhecimento direto da origem do envelhecimento e da morte, conhecimento direto
da cessação do envelhecimento e da morte, conhecimento direto do caminho que
leva à cessação do envelhecimento e da morte. Cheguei ao conhecimento direto do
nascimento ... ser/existir ... apego ... desejo ... sensação ... contato ... as seis
bases dos sentidos ... mentalidade-materialidade (nome e forma) ... consciência, conhecimento
direto das formações volitivas, conhecimento direto da origem das formações volitivas, conhecimento
direto da cessação das formações volitivas, conhecimento direto do caminho que leva à
cessação das formações volitivas. Tendo o conhecimento direto disso, eu o revelei aos
bhikkhus, bhikkhunis, discípulos leigos, para que esta vida santa se torne bem
sucedida e próspera, extensa, popular, expandida, bem proclamada entre os devas
e humanos."<o:p></o:p></span></p>



