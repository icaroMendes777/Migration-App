<p class=Normal> "Venerável senhor, é dito: 'Cisma na Sangha, cisma na sangha.' De que modo, venerável senhor há um cisma na Sangha?" </p>

<p class=Normal> "Aqui, Upali, (1) os bhikkhus explicam o não-Dhamma como Dhamma, (2) e Dhamma como não-Dhamma. (3) Eles explicam a não-Disciplina como Disciplina, e (4) a Disciplina como não-Disciplina. (5) Eles explicam aquilo que não foi dito e pronunciado pelo Tathagata como tendo sido dito e pronunciado por ele, e (6) aquilo que foi dito e pronunciado pelo Tathagata como não tendo sido dito e pronunciado por ele. (7) Eles explicam aquilo que não foi praticado pelo Tathagata como tendo sido praticado por ele, e (8) aquilo que foi praticado pelo Tathagata como não tendo sido praticado por ele. (9) Eles explicam aquilo que não foi prescrito pelo Tathagata como tendo sido prescrito por ele, e (10) aquilo que foi prescrito pelo Tathagata como não tendo sido prescrito por ele. Com base nessas dez razões eles se distanciam e se separam. Eles realizam os atos legais separadamente e recitam o Patimokkha separadamente. Desse modo, Upali, há cisma na Sangha." </p>


