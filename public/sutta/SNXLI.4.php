<p class=Normal>Certa ocasião um número de bhikkhus seniores estavam no mangueiral Silvestre em Macchikasanda. Então Citta, o chefe de família, foi até esses bhikkhus seniores e depois de cumprimentá-los sentou a um lado e disse: “Veneráveis senhores, que os veneráveis concordem em aceitar de mim a refeição de amanhã no meu estábulo.” </p>
 
<p class=Normal>Os bhikkhus seniores concordaram em silêncio. Então Citta, sabendo que eles haviam concordado, levantou do seu assento e depois de homenageá-los, mantendo-os à sua direita, partiu. </p>  

<p class=Normal>Então, quando havia terminado a noite, ao amanhecer, os  bhikkhus seniores vestiram os seus mantos e carregando as suas tigelas e os mantos externos foram até o estábulo do chefe de família Citta, sentando nos assentos que haviam sido preparados. </p>

<p class=Normal>Então, com as próprias mãos, o chefe de família Citta serviu e satisfez os bhikkhus seniores com um delicioso arroz com leite feito com manteiga clarificada. Quando os bhikkhus seniores haviam terminado de comer e tendo colocado as tigelas de lado, eles levantaram dos seus assentos e partiram. </p>

<p class=Normal>Então, Citta, tendo dito, “Dê aquilo que sobrou,” seguiu de perto os bhikkhus seniores. Agora, naquela ocasião, fazia um calor intenso, e os veneráveis caminhavam como se os seus corpos estivessem derretendo devido ao que haviam comido. Agora, naquela ocasião, o Venerável Mahaka era o bhikkhu mais júnior daquela Sangha. Então o Venerável Mahaka disse para o mais sênior daqueles bhikkhus: “Seria bom, venerável senhor, se um brisa fresca soprasse e uma cobertura de nuvens se formasse e que do céu caísse uma garoa.” </p>

<p class=Normal> “Isso seria muito bom, amigo.” </p>

<p class=Normal>Assim, o Venerável Mahaka realizou tamanha façanha que uma brisa fresca soprou e uma cobertura de nuvens se formou e do céu caiu uma garoa. </p>

<p class=Normal>Então, Citta pensou: “Esse é o poder e a força espiritual que possui o bhikkhu mais júnior dessa Sangha!” </p>

<p class=Normal>Chegando ao monastério o Venerável Mahaka perguntou ao bhikkhu mais sênior: “Isso foi o suficiente, Venerável senhor?” </p>

<p class=Normal> “Isso é o suficiente, amigo Mahaka. Aquilo que foi feito é o suficiente, amigo Mahaka, aquilo que foi oferecido é o suficiente.” </p>

<p class=Normal>Então, os bhikkhus seniores foram para as suas moradias e o Venerável Mahaka foi para a moradia dele. </p>

<p class=Normal>Então, Citta foi até o Venerável Mahaka e depois de cumprimentá-lo sentou a um lado e disse: “Seria bom, Venerável senhor, se o Mestre Mahaka me mostrasse uma façanha supra-humana.” </p>

<p class=Normal> “Então, chefe de família, abra a sua capa na varanda e espalhe um punhado de capim sobre ela.” </p>

<p class=Normal> “Sim, venerável senhor,” Citta respondeu e ele abriu a sua capa na varanda e espalhou um punhado de capim sobre ela. </p> 

<p class=Normal>Em seguida, depois de ter entrado na sua moradia, o Venerável Mahaka fechou a porta com um ferrolho e realizou tamanha façanha supra-humana que uma chama atravessou pelo buraco da fechadura e pelas fissuras da porta e queimou o capim mas não a capa. O chefe de família Citta sacudiu a sua capa e ficou em pé a um lado, chocado e aterrorizado. </p>

<p class=Normal>Então, o Venerável Mahaka saiu da sua moradia e disse para Citta: “Isso foi o suficiente, chefe de família?” </p>

<p class=Normal> “Isso é o suficiente, Venerável Mahaka. Aquilo que foi feito é o suficiente, Venerável Mahaka, aquilo que foi oferecido é o suficiente. Que o Mestre Mahaka desfrute do prazeroso mangueiral Silvestre em Macchikasanda. Eu serei diligente em prover o Mestre Mahaka com mantos, comida, moradia e medicamentos.” </p>

<p class=Normal> “Isso é dito com bondade, chefe de família.” </p>

<p class=Normal>Depois, o Venerável Mahaka arrumou a sua moradia e tomando a tigela e o manto externo, partiu de  Macchikasanda. Ao ter deixado Macchikasanda, ele partiu em definitivo e nunca mais regressou. </p> 

