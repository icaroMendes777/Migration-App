<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. Então, um certo bhikkhu foi até o Abençoado e depois de
cumprimentá-lo sentou a um lado e disse:<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, dizem, ‘um orador do Dhamma, um orador do Dhamma.’ De que forma,
venerável senhor, alguém é um orador do Dhamma?”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhu,
se um bhikkhu ensina o Dhamma tendo como propósito o desencantamento em relação
à forma, o seu desaparecimento e cessação, ele pode ser chamado um bhikkhu que
é um orador do Dhamma. Se um bhikkhu pratica tendo como propósito o
desencantamento em relação à forma, pelo seu desaparecimento e cessação, ele
pode ser chamado um bhikkhu que está praticando de acordo com o Dhamma. Se,
através do desencantamento com a forma, através do seu desaparecimento e
cessação, ele estiver libertado através do desapego, ele pode ser chamado um
bhikkhu que realizou Nibbana nesta mesma vida. <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhu,
se alguém ensina o Dhamma tendo como propósito o desencantamento em relação à
sensação ... percepção ... formações volitivas ... consciência, pelo seu desaparecimento
e cessação, ele pode ser chamado um bhikkhu que é um orador do Dhamma. Se
alguém pratica tendo como propósito o desencantamento em relação à consciência,
pelo seu desaparecimento e cessação, ele pode ser chamado um bhikkhu que está
praticando de acordo com o Dhamma. Se, através do desencantamento com a
consciência, através do seu desaparecimento e cessação, ele estiver libertado
através do desapego, ele pode ser chamado um bhikkhu que realizou Nibbana nesta
mesma vida. <b style='mso-bidi-font-weight:normal'><o:p></o:p></b></span></p>


