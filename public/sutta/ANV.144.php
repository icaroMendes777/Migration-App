<p class=Normal> Em certa ocasião o Abençoado estava em Saketa, no Bosque Tikandaki. Lá ele se dirigiu aos monges desta forma: "Bhikkhus." – "Venerável Senhor," eles responderam. O Abençoado disse o seguinte: </p>

<p class=Normal> "(1) Bhikkhus, é bom que um bhikkhu de tempos em tempos permaneça percebendo o repulsivo no não repulsivo. (2) É bom que um bhikkhu de tempos em tempos permaneça percebendo o não repulsivo no repulsivo. (3) É bom que um bhikkhu de tempos em tempos permaneça percebendo o repulsivo em ambos no repulsivo e no não repulsivo. (4) É bom que um bhikkhu de tempos em tempos permaneça percebendo o não repulsivo em ambos no repulsivo e no não repulsivo. (5) É bom que um bhikkhu de tempos em tempos permaneça com equanimidade, com atenção plena e plena consciência, tendo se afastado de ambos o repulsivo e o não repulsivo. [<a href="#N1">1</a>]</p>

<p class=Normal> <a name=R1></a> (1) "E  por qual benefício deve um bhikkhu permanecer percebendo o repulsivo no não repulsivo? 'Que nenhuma cobiça surja em mim com relação a coisas que provocam a cobiça!': por esse benefício um bhikkhu deve permanecer percebendo o repulsivo no não repulsivo. </p>

<p class=Normal> (2) "E  por qual benefício deve um bhikkhu permanecer percebendo o não repulsivo no repulsivo? 'Que nenhuma raiva surja em mim com relação a coisas que provocam a raiva!': por esse benefício um bhikkhu deve permanecer percebendo o não repulsivo no repulsivo. </p>

<p class=Normal> (3) "E  por qual benefício deve um bhikkhu permanecer percebendo o repulsivo em ambos no repulsivo e no não repulsivo? 'Que nenhuma cobiça surja em mim com relação a coisas que provocam a cobiça, e que nenhuma raiva surja em mim com relação a coisas que provocam a raiva!': por esse benefício um bhikkhu deve permanecer percebendo o repulsivo em ambos no repulsivo e no não repulsivo. </p>

<p class=Normal> (4) "E  por qual benefício deve um bhikkhu permanecer percebendo o não repulsivo em ambos no repulsivo e no não repulsivo? 'Que nenhuma raiva surja em mim com relação a coisas que provocam a raiva, e que nenhuma cobiça surja em mim com relação a coisas que provocam a cobiça!': por esse benefício um bhikkhu deve permanecer percebendo o não repulsivo em ambos no repulsivo e no não repulsivo. </p>

<p class=Normal> (5) "E  por qual benefício deve um bhikkhu permanecer com equanimidade, com atenção plena e plena consciência, tendo se afastado de ambos o repulsivo e o não repulsivo? 'Que nenhuma cobiça em absoluto surja em mim com relação a coisas que provocam a cobiça! Que nenhuma raiva em absoluto surja em mim com relação a coisas que provocam a raiva! Que nenhuma delusão em absoluto surja em mim com relação a coisas que provocam a delusão!': por esse benefício um bhikkhu deve permanecer com equanimidade, com atenção plena e plena consciência, tendo se afastado de ambos o repulsivo e o não repulsivo." </p>

