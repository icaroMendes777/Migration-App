<p class=Normal> 
Bhikkhus, há esses três tipos de perfeição. Quais três? Perfeição
corporal, perfeição verbal e perfeição mental. E o que é perfeição corporal?
É o caso em que o bhikhhu se abstém de tirar a vida de outros seres, se abstém
de roubar, se abstém de não manter a castidade. A isto se chama perfeição
corporal.<o:p></o:p></span></p>


<p class=Normal>
E o que é perfeição verbal? É o caso em que o bhikhhu se abstém da
linguagem mentirosa, se abstém da linguagem maliciosa, se abstém da linguagem
grosseira, se abstém da linguagem frívola. A isto se chama perfeição verbal.<o:p></o:p></span></p>


<p class=Normal>
E o que é perfeição mental? É o caso em que um bhikkhu<span
style="mso-spacerun: yes">  </span>– com a eliminação das impurezas mentais <i
style='mso-bidi-font-style:normal'>(asava)</i><span style="mso-spacerun: yes"> 
</span>– permanece em um estado livre de impurezas com a libertação da mente e
a libertação através da sabedoria, tendo conhecido e manifestado isso para si mesmo
no aqui e agora. A isto se chama perfeição mental.<o:p></o:p></span></p>


<p class=Normal>
Essas, bhikkhus, são as três formas de perfeição.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>Um sábio em relação ao corpo, um sábio em relação à
palavra,<br>

um sábio em relação à mente, sem impurezas:<br>

um sábio consumado na perfeição<br>

se diz que abandonou<br>

tudo.<o:p></o:p></span></p>

