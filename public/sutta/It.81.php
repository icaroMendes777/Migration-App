<p class=Normal> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: "Bhikkhus, eu vi seres subjugados e com as mentes obcecadas em receber homenagens, com a dissolução do corpo, após a morte, eles renasceram num estado de privação, num destino infeliz, nos reinos inferiores, até mesmo no inferno. Eu vi seres subjugados e com as mentes obcecadas por não receber homenagens ... por ambos, em receber homenagens e por não receber homenagens, com a dissolução do corpo, após a morte, eles renasceram num estado de privação, num destino infeliz, nos reinos inferiores, até mesmo no inferno.</p>

<p class=Normal> "Eu digo isso, bhikkhus, não por ter ouvido de algum contemplativo ou brâmane que seres subjugados .... até mesmo no inferno. Mas é porque eu mesmo sei isso, vi isso, observei isso, que eu digo: eu vi seres subjugados ... até mesmo no inferno."</p>

<p class=Normal style='margin-left:.75in'> Ele permanece diligente<br>
com  a concentração imperturbável, <br>
ambos quando é homenageado<br>
e quando não recebe nenhuma homenagem.</p>

