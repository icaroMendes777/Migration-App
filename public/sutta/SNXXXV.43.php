<p class=Normal> Em Savatthi. </p>

<p class=Normal> “Bhikkhus, o todo é impermanente ... o todo é sofrimento ... o todo é não-eu ... o todo é para ser conhecido diretamente ... o todo é para ser completamente compreendido ... o todo é para ser abandonado ... o todo é para ser realizado ... o todo é para ser completamente compreendido através do conhecimento direto ... o todo é problemático ... o todo é aflição ..." </p>

<p class=Normal> “O olho ... As formas ... A consciência no olho ... O contato no olho ... Qualquer sensação que surja tendo o contato no olho como condição - experimentada como prazer, dor ou nem prazer, nem dor - isso também é impermanente ... é aflição. O ouvido ... O nariz ... A língua ... O corpo ... A mente ... Os objetos mentais ... A consciência na mente ... O contato na mente ... Qualquer sensação que surja tendo o contato na mente como condição - experimentada como prazer, dor ou nem prazer, nem dor - isso também é impermanente ... é aflição.</p>

<p class=Normal> “Vendo desse modo, o nobre discípulo bem instruído se desencanta com o olho, com as formas, com a consciência no olho, com o contato no olho, com qualquer sensação que surja tendo o contato no olho como condição - experimentada como prazer, dor ou nem prazer, nem dor. Ele se desencanta com o nariz ... com o ouvido ... com a língua ... com o corpo ...... com a mente, com os objetos mentais, com a consciência na mente, com o contato na mente, com qualquer sensação que surja tendo o contato na mente como condição - experimentada como prazer, dor ou nem prazer, nem dor. Desencantado ele se torna desapegado. Através do desapego a sua mente é libertada. Quando ela está libertada surge o conhecimento: ‘Libertada.’ Ele compreende que: ‘O nascimento foi destruído, a vida santa foi vivida, o que deveria ser feito foi feito, não há mais vir a ser a nenhum estado.’”</p>

