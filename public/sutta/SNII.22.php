<p class=Normal>Ficando em pé a um lado a devata Khema disse: </p>

<p class=Normal style='margin-left:.75in'>"Tolos desprovidos de sabedoria<br>
se comportam como seus próprios inimigos<br>
ocupando-se com ações prejudiciais<br>
que apenas produzem frutos amargos. </p>

<p class=Normal style='margin-left:.75in'>"Uma ação não é bem feita<br>
quando, tendo sido realizada, produz arrependimento, <br>
cujo resultado é experimentado<br>
numa face molhada de lágrimas. </p>

<p class=Normal style='margin-left:.75in'>"Mas uma ação é bem feita<br>
quando, tendo sido realizada, não produz arrependimento, <br>
cujo resultado é experimentado<br>
com alegria numa mente feliz." </p>

<p class=Normal>O Abençoado: </p>

<p class=Normal style='margin-left:.75in'>"Alguém prontamente deve agir<br>
quando sabe que aquilo conduz ao próprio bem-estar; <br>
o pensador, o sábio, não deve avançar<br>
com o pensamento do cocheiro. </p>

<p class=Normal style='margin-left:.75in'>"Como o cocheiro que sai da estrada, <br>
um caminho plano, <br>
e tendo entrado num desvio irregular<br>
triste, ensimesmado, com um eixo quebrado -</p>

<p class=Normal style='margin-left:.75in'>"Assim o tolo, tendo deixado o Dhamma<br>
para seguir um caminho oposto ao Dhamma, <br>
quando cai nas garras da morte<br>
se ensimesma como o cocheiro com um eixo quebrado." </p>



