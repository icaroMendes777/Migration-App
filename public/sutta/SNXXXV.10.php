<p class=Normal> “Bhikkhus, as formas são impermanentes, ambos no passado e no futuro, sem falar no presente. Vendo assim, bhikkhus, o nobre discípulo bem instruído é indiferente com relação às formas do passado; ele não busca o prazer nas formas no futuro; e ele pratica pelo desencantamento com relação às formas no presente, pelo desaparecimento e cessação. </p>

