<p class=Normal>"Bhikkhus, há esses dois poderes. Quais dois? O poder da reflexão e o poder da meditação. </p>

<p class=Normal> (11, 12, 13) "E qual é o poder da reflexão? Aqui, alguém reflete assim: 'A conduta corporal imprópria tem um resultado ruim na vida presente e na vida futura; a conduta verbal imprópria tem um resultado ruim na vida presente e na vida futura; a conduta mental imprópria tem um resultado ruim na vida presente e na vida futura. Tendo assim refletido, ele abandona a conduta corporal imprópria e desenvolve a conduta corporal apropriada, ele abandona a conduta verbal imprópria e desenvolve a conduta verbal apropriada; ele abandona a conduta mental imprópria e desenvolve a conduta mental apropriada; ele se mantém na pureza. Esse é chamado o poder de reflexão. </p>

<p class=Normal><a name=N1></a> (11) "E qual é o poder da meditação? O poder da meditação é o poder dos treinandos. [<a href="#N1">1</a>] Baseando-se no poder de um treinando, ele abandona a cobiça, a raiva e a delusão, e não age de forma prejudicial: não se dedica a nada ruim. Esse é chamado o poder da meditação. </p>

<p class=Normal> (12) "E qual é o poder da meditação? Aqui, um bhikkhu desenvolve o fator da iluminação da atenção plena, que tem como base o afastamento, desapego e cessação que amadurece no abandono. Ele desenvolve o fator da iluminação da investigação dos fenômenos ... Ele desenvolve o fator da iluminação da energia ... Ele desenvolve o fator da iluminação do êxtase ... Ele desenvolve o fator da iluminação da tranqüilidade ... Ele desenvolve o fator da iluminação da concentração ... Ele desenvolve o fator da iluminação da equanimidade, que tem como base o afastamento, desapego e cessação que amadurece no abandono. Esse é chamado o poder da meditação.</p>

<p class=Normal> (13) "E qual é o poder da meditação? Aqui, afastado dos prazeres sensuais, afastado das qualidades não hábeis, um bhikkhu entra e permanece no primeiro jhana, que é caracterizado pelo pensamento aplicado e sustentado, com o êxtase e felicidade nascidos do afastamento. Abandonando o pensamento aplicado e sustentado, ele entra e permanece no segundo jhana, que é caracterizado pela segurança interna e perfeita unicidade da mente, sem o pensamento aplicado e sustentado, com o êxtase e felicidade nascidos da concentração. Abandonando o êxtase, ele entra e permanece no terceiro jhana que é caracterizado pela felicidade sem o êxtase, acompanhada pela atenção plena, plena consciência e equanimidade, acerca do qual os nobres declaram: ‘Ele permanece numa estada feliz, equânime e plenamente atento.’ Com o completo desaparecimento da felicidade, ele entra e permanece no quarto jhana, que possui nem felicidade nem sofrimento, com a atenção plena e a equanimidade purificadas. Esse é chamado o poder da meditação.</p>

<p class=Normal>(11, 12, 13) "Estes bhikkhus, são os dois poderes." </p>

