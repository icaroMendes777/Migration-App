<p class=Normal>"Bhikkhus há esses quatro objetos
supremos de fé. Quais quatro? </p>

<p class=Normal> “Entre
todos os seres que possam existir – sem patas, com duas patas, quatro patas,
muitas patas; com forma ou sem forma; perceptivos, não perceptivos, nem
perceptivos nem não perceptivos – o Tathagata, um arahant, perfeitamente iluminado,
é considerado supremo. Aqueles que têm fé no Iluminado depositam fé naquilo que
é supremo; e para aqueles que têm fé no que é supremo, supremo será o
resultado. [<a href="#N1">1</a>]</p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><a name=R1></a>“Entre
todos os fenômenos que são condicionados, o Nobre Caminho Óctuplo -
entendimento correto, pensamento correto, linguagem correta, ação correta, modo
de vida correto, esforço correto, atenção plena correta, concentração correta –
é considerado supremo. Aqueles que têm fé no Nobre Caminho Óctuplo têm fé
naquilo que é supremo; e para aqueles que têm fé no que é supremo, supremo será
o resultado.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><a name=R2></a>“Entre
todos os fenômenos que são condicionados ou não-condicionados, o desapego é declarado como supremo dentre todos, isto é, subjugar a presunção, remover as impurezas, remover a permanência, [<a href="#N2">2</a>] interromper o ciclo, destruir o desejo, desapego, cessação, nibbana. Aqueles que têm convicção no desapego têm convicção no supremo e para aqueles que têm convicção no que é supremo, supremo será o
resultado. </p><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Entre
todas as comunidades ou grupos que possam haver, a Sangha dos discípulos do
Tathagata é considerada suprema – isto é, os quatro [grupos dos nobres
discípulos] quando tomados como pares, os oito quando tomados como indivíduos.
Aqueles que têm fé na Sangha têm fé naquilo que é supremo; e para aqueles que têm
fé no que é supremo, supremo será o resultado.<o:p></o:p></span></p>

