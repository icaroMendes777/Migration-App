<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em certa
ocasião, o Abençoado estava em Nalanda no Manguezal de Pavarika. Então, o chefe
tribal Asibandhakaputta foi até o Abençoado e depois de cumprimentá-lo sentou a
um lado e disse: <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, o Abençoado não permanece com compaixão por todos os seres sencientes?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim, chefe
tribal, o Tathagata permanece com compaixão por todos os seres sencientes.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Porque
então, venerável senhor, que o Abençoado ensina o Dhamma a fundo para alguns,
mas não tão a fundo para outros?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bem, chefe
tribal, então eu lhe farei uma pergunta com relação a isso. Responda como
julgar adequado. O que você pensa, chefe tribal? Suponha que um agricultor
tivesse três campos: um excelente, um com qualidade média e um inferior, com a
terra ruim, irregular, salobre. O que você pensa, chefe tribal? Se aquele
agricultor quiser semear, onde ele semearia primeiro: no campo excelente, no
campo com qualidade média ou no campo inferior, aquele com a terra ruim,
irregular, salobre?” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Se,
venerável senhor, aquele agricultor quiser semear, ele semearia no campo
excelente. Depois de ali semear, ele em seguida semearia no campo com qualidade
média. Depois de ali semear, ele poderá semear, ou não, no campo inferior,
aquele com a terra ruim, irregular, salobre. Por qual razão? Porque pelo menos
ele poderá ser usado como alimento para o gado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Chefe
tribal, como o campo que é excelente são os bhikkhus e as bhikkhunis para mim.
Eu ensino para eles o Dhamma que é admirável no início, admirável no meio, e
admirável no final, com o significado e fraseado corretos; eu revelo a vida
santa que é completamente perfeita e imaculada. Por qual razão? Porque eles
permanecem tendo a mim como sua ilha, tendo a mim como seu abrigo, tendo a mim
como seu protetor, tendo a mim como seu refúgio. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
chefe tribal, tal como o campo com qualidade média tenho discípulos leigos.
Para eles eu também ensino o Dhamma que é admirável no início, admirável no
meio, e admirável no final, com o significado e fraseado corretos; eu revelo a
vida santa que é completamente perfeita e imaculada. Por qual razão? Porque
eles permanecem tendo a mim como sua ilha, tendo a mim como seu abrigo, tendo a
mim como seu protetor, tendo a mim como seu refúgio.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
chefe tribal, tal como o campo inferior, com a terra ruim, irregular, salobre –
há os contemplativos, brâmanes e errantes de outras seitas. Apesar disso eu
também ensino para eles o Dhamma que é admirável no início, admirável no meio,
e admirável no final, com o significado e fraseado corretos; eu revelo a vida
santa que é completamente perfeita e imaculada. Por qual razão? Porque se eles
entenderem apenas uma única sentença, isso irá conduzir ao seu bem-estar e
felicidade por muito tempo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Suponha,
chefe tribal, que um homem possuísse três jarros com água: um sem rachaduras,
que não permite que a água vaze e escape; um sem rachaduras, mas que permite
que a água vaze e escape; e um com rachaduras, que permite que a água vaze e
escape. O que você pensa, chefe tribal? Se um homem quiser armazenar água, onde
ele deveria armazenar primeiro: no jarro sem rachaduras, que não permite que a
água vaze e escape; ou no jarro sem rachaduras, mas que permite que a água vaze
e escape; ou no jarro com rachaduras, que permite que a água vaze e escape?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se,
venerável senhor, aquele homem quisesse armazenar água, ele armazenaria no
jarro sem rachaduras, que não permite que a água vaze e escape. Tendo
armazenado nesse, ele em seguida armazenaria no jarro sem rachaduras, mas que
permite que a água vaze e escape. Tendo armazenado nesse, ele poderá armazenar,
ou não, no jarro com rachaduras, que permite que a água vaze e escape. Por qual
razão? <span style="mso-spacerun: yes"> </span>Porque pelo menos este poderá
ser usado para lavar a louça.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Chefe
tribal, tal como o jarro sem rachaduras, que não permite que a água vaze e
escape, são para mim os bhikkhus e as bhikkhunis. Eu ensino para eles o Dhamma
que é admirável no início, admirável no meio, e admirável no final, com o
significado e fraseado corretos; eu revelo a vida santa que é completamente
perfeita e imaculada. Por qual razão? Porque eles permanecem tendo a mim como
sua ilha, tendo a mim como seu abrigo, tendo a mim como seu protetor, tendo a
mim como seu refúgio. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Depois,
chefe tribal, tal como o jarro sem rachaduras, mas que permite que a água vaze
e escape, são para mim os discípulos leigos. Para eles eu também ensino o
Dhamma que é admirável no início, admirável no meio, e admirável no final, com
o significado e fraseado corretos; eu revelo a vida santa que é completamente
perfeita e imaculada. Por qual razão? Porque eles permanecem tendo a mim como
sua ilha, tendo a mim como seu abrigo, tendo a mim como seu protetor, tendo a
mim como seu refúgio.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Depois,
chefe tribal, tal como o jarro com rachaduras, que permite que a água vaze e
escape, são para mim os contemplativos, brâmanes e errantes de outras seitas.
Apesar disso eu também ensino para eles o Dhamma que é admirável no início,
admirável no meio, e admirável no final, com o significado e fraseado corretos;
eu revelo a vida santa que é completamente perfeita e imaculada. Por qual
razão? Porque se eles entenderem apenas uma única sentença, isso irá conduzir
ao seu bem-estar e felicidade por muito tempo.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isso
foi dito o chefe tribal Asibandhakaputta disse para o Abençoado: “Magnífico,
Venerável senhor! Magnífico, Venerável senhor! O Abençoado esclareceu o Dhamma
de várias formas, como se tivesse colocado em pé o que estava de cabeça para
baixo, revelasse o que estava escondido, mostrasse o caminho para alguém que
estivesse perdido ou segurasse uma lâmpada no escuro para aqueles que possuem
visão pudessem ver as formas. Nós buscamos refúgio no Abençoado, no Dhamma e na
Sangha dos bhikkhus. Que o Abençoado nos aceite como discípulo leigo que nele
buscou refúgio para o resto da vida.”<o:p></o:p></span></p>

