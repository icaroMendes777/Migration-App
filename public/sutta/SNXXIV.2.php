palavras. Tendo ouvido do Abençoado, os bhikkhus o recordarão.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando há
forma, bhikkhus, através do apego à forma, devido à adesão à forma, uma idéia
como essa surge: ‘Isso é meu, isso sou eu, isso é o meu eu.’ Quando há sensação
... percepção ... formações ... consciência, através do apego à consciência,
devido à adesão à consciência, uma idéia como essa surge: ‘Isso é meu, isso sou
eu, isso é o meu eu.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando,
