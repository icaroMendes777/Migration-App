<p class=Normal>Assim ouvi. Certa ocasião, o Abençoado estava em Uruvela às margens do rio Neranjara ao pé de uma figueira-dos-pagodes, pouco tempo depois de ter alcançado a perfeita iluminação - ele ficou sentado em meditação sob a sombra dessa árvore por sete dias sem parar, sensível à felicidade da libertação. Ao final de sete dias, o Abençoado emergiu daquela concentração.</p>


<p class=Normal>Então, um certo brâmane arrogante e prepotente, recitador de mantras, foi até o Abençoado e ambos se cumprimentaram. Quando a conversa amigável e cortês havia terminado, ele sentou a um lado e disse:</p>


<p class=Normal>“De que forma, caro Gotama, alguém é um brâmane? Mais ainda, o que faz desse alguém um brâmane?”</p>



<p class=Normal>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p>



	<p class=Normal style='margin-left:.75in'>“Um brâmane é alguém que se encontra livre de más qualidades, <br>

	ele está livre da arrogância e prepotência, livre de impurezas, e tem a sua mente contida.<br>

	Dotado de perfeito entendimento, ele levou à conclusão a vida santa.<br>

	Justa e corretamente tal brâmane profere a doutrina dos nobres,<br>

	pois ele se encontra livre de qualquer arrogância com relação ao mundo."</p>

