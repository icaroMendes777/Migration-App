<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião, o Abençoado estava perambulando por Kosala com uma
grande sangha de bhikkhus, com quinhentos bhikkhus, até que por fim acabou
chegando em Salavatika. Naquela ocasião, o Brâmane Lohicca estava vivendo em
Salavatika, uma propriedade real com muitos habitantes, rica em pastagens,
árvores, rios e grãos, uma concessão real, uma doação sagrada que lhe foi dada
pelo rei Pasenadi de Kosala. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. Agora,
naquela ocasião, uma idéia perniciosa havia surgido na mente de Lohicca:
“Suponha que um contemplativo ou Brâmane descubra uma certa doutrina benéfica [<a
href="#N1">1</a>] <a name=R1></a>e <span style='mso-spacerun:yes'> </span>que tendo
feito isso ele não a declare para ninguém; pois o que pode um homem fazer por
outro? É como se um homem, tendo rompido um antigo grilhão, criasse um novo. Eu
declaro que esse tipo de coisa é uma ação ruim enraizada no apego, pois o que
pode um homem fazer por outro?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. Então,
Lohicca ouviu que o contemplativo Gotama havia chegado em Salavatika, e acerca
desse mestre Gotama existe essa boa reputação ... (igual ao <a href="DN4.php#T2">DN
4.2</a>). É bom poder encontrar alguém tão nobre.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. E
Lohicca disse para o barbeiro Bhesika: “Amigo Bhesika, vá até onde está o
contemplativo Gotama e pergunte se ele está livre de enfermidades e aflições,
se está com saúde, forte e vivendo com conforto, dizendo: “Que o Mestre Gotama
e a Sangha dos bhikkhus concorde em aceitar a refeição de amanhã do Brâmane
Lohicca.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. “Muito
bem, senhor,” respondeu Bhesika, e executou a sua tarefa. O Abençoado concordou
em silêncio.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. Então,
sabendo que o Abençoado havia concordado, Bhesika levantou-se do seu assento, e
depois de homenagear o Abençoado, mantendo-o à sua direita, partiu. Ele foi até
onde estava Lohicca e relatou que o Abençoado havia concordado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. Então,
quando a noite terminou Lohicca fez com que se preparassem vários tipos de boa
comida na sua própria residência e quando tudo estava pronto ele mandou Bhesika
anunciar que a refeição estava pronta. O Abençoado se vestiu e
carregando a sua tigela e o manto externo, foi com a Sangha dos bhikkhus para
Salavatika.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. Bhesika
seguiu o Abençoado de perto. E ele disse: “Venerável senhor, esta idéia
perniciosa surgiu na mente do Brâmane Lohicca ... deveras, venerável senhor,
isso é o que o Brâmane Lohicca está pensando.”<br> “Pode muito bem ser assim,
Bhesika, pode muito bem ser assim.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. Então, o
Abençoado chegou na casa de Lohicca e sentou num assento que havia sido
preparado. Então, com as próprias mãos Lohicca<span style='mso-spacerun:yes'> 
</span>serviu e satisfez o Abençoado e os bhikkhus com os vários tipos de boa
comida. Em seguida, quando o Abençoado havia terminado de comer e retirado a
mão da sua tigela, Lohicca sentou a um lado, num assento mais baixo, e o
Abençoado disse: “Lohicca, é verdade que<span style='mso-spacerun:yes'> 
</span>esta idéia perniciosa surgiu na sua mente ... (igual ao verso 2)?”<br> “Sim,
Venerável Gotama.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. “O que
você pensa, Lohicca? Você tem soberania sobre Salavatika?”<br> “Sim, Venerável
Gotama.”<br> “Muito bem, então se alguém dissesse: ‘O Brâmane Lohicca tem soberania
sobre Salavatika e ele deve desfrutar por inteiro de todos os frutos e receitas
de Salavatika, sem dar nada aos outros’ – alguém que assim falasse não seria
uma ameaça para os habitantes de<span style='mso-spacerun:yes'> 
</span>Salavatika?”<br> “Ele seria uma ameaça, Venerável Gotama.”<br>

“E como
tal, ele estaria preocupado com o bem-estar deles ou não?”<br> “Ele não estaria,
Venerável Gotama.”<br>

“E, não
estando preocupado com o bem-estar deles, o coração dele estaria pleno de amor
ou de má vontade?”<br> “Má vontade, Venerável Gotama.”<br>

“E num
coração pleno de má vontade, há o entendimento incorreto ou o entendimento
correto?”<br> “Entendimento incorreto, Venerável Gotama.”<br>

“Mas
Lohicca, eu declaro que o entendimento incorreto conduz a um dos dois destinos
– o inferno ou o renascimento como um animal.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. <span
style='mso-spacerun:yes'> </span>“O que você pensa, Lohicca? O Rei Pasenadi de
Kosala tem soberania sobre Kasi-Kosala?”<br> “Ele tem, Venerável Gotama.”<br> “Muito
bem, então se alguém dissesse: ‘O Rei Pasenadi de Kosala tem soberania sobre
Kasi-Kosala, ele deve desfrutar por inteiro de todos os frutos e receitas de
Kosala, sem dar nada aos outros’ – alguém que assim falasse não seria uma
ameaça para os habitantes de Kosala? ... o coração dele não estaria pleno de
má vontade ... e isso seria entendimento incorreto?”<br> “Seria, Venerável Gotama.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12.
“Portanto, Lohicca, você concorda que com certeza qualquer um que diga o mesmo
do Brâmane Lohicca ...<span style='mso-spacerun:yes'>  </span>isso seria
entendimento incorreto.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. “Do
mesmo modo, Lohicca, se alguém dissesse: ‘Suponha que um contemplativo ou
Brâmane descubra uma certa doutrina benéfica e que tendo feito isso ele não a
declare para ninguém; pois o que pode um homem fazer por outro?’ ele seria uma
ameaça para os jovens de boas famílias que, seguindo o Dhamma e Disciplina
ensinado pelo Tathagata, atingem sucessivos estágios de distinção como a
realização do fruto do entrar na correnteza, do retornar uma vez, do não
retornar e do arahant - e de todos aqueles cujos frutos da conduta amadurecerão
num renascimento nos mundos dos devas. [<a href="#N2">2</a>] <a name=R2></a>Sendo
uma ameaça, ele não tem amor bondade e o seu coração está enraizado na má vontade e
isso constitui<span style='mso-spacerun:yes'>  </span>entendimento incorreto,
que conduz a um dos dois destinos – o inferno ou o renascimento como um animal.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14. “E
qualquer um que diga o mesmo do Rei Pasenadi, seria uma ameaça para os súditos
do Rei, você e os outros ... <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. (igual
ao verso 13) <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16.
“Lohicca, há esses três tipos de mestres no mundo que são censuráveis e
qualquer um que critique esses mestres, a sua crítica será apropriada,
verdadeira, de acordo com a realidade e justificável. Quais três? É o caso,
Lohicca, em que há um tipo de mestre que deixou a vida em família e seguiu a
vida santa, mas que não alcançou o objetivo da vida contemplativa. E sem ter
alcançado esse objetivo, ele ensina os seus discípulos uma doutrina, dizendo:
‘Isso é pelo seu bem, isso é para a sua felicidade.’ Mas os seus discípulos não
querem ouvir, eles não dão ouvidos, eles não despertam o pensamento da
iluminação e as instruções do mestre são desprezadas. Ele deve ser criticado,
dizendo: ‘Este venerável deixou a vida em família ... as suas instruções são
desprezadas.’ É como se um homem insistisse nos galanteios a uma mulher que o
rejeita e tentasse abraçá-la embora ela o afaste.’ Eu declaro ser essa uma
doutrina ruim enraizada no apego, pois o que pode esse homem fazer por outro?
Esse é o primeiro mestre censurável ...<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17. “É o
caso, em que há um tipo de mestre que deixou a vida em família e seguiu a
vida santa, mas que não alcançou o objetivo da vida contemplativa. E sem ter
alcançado esse objetivo, ele ensina os seus discípulos uma doutrina, dizendo:
‘Isso é pelo seu bem, isso é para a sua felicidade.’ E os seus discípulos querem ouvir, eles dão ouvidos,
eles despertam o pensamento da iluminação e as instruções do mestre não são
desprezadas. Ele deve ser criticado, dizendo: ‘Este venerável deixou a vida em
família ... as suas instruções não são
desprezadas.’ É como se negligenciando o seu próprio terreno, ele<span
style='mso-spacerun:yes'>  </span>pensasse que o terreno do vizinho precisasse
ser limpo de ervas daninhas. Eu declaro ser essa uma doutrina ruim enraizada no
apego, pois o que pode esse homem fazer por outro? Esse é o segundo mestre que
é censurável ...<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18. “É o
caso, em que há um tipo de mestre que deixou a vida em família e seguiu a
vida santa, e que alcançou o objetivo da vida contemplativa. Tendo
alcançado esse objetivo, ele ensina os seus discípulos uma doutrina, dizendo:
‘Isso é pelo seu bem, isso é para a sua felicidade.’ E os seus discípulos não
querem ouvir, eles não dão ouvidos, eles não despertam o pensamento da
iluminação e as instruções do mestre são desprezadas. Ele também deve ser
criticado dizendo: ‘Este venerável deixou a vida em
família ... as suas instruções são
desprezadas.’ É como se tendo rompido um antigo grilhão, fosse criado um novo.
Eu declaro ser essa uma doutrina ruim enraizada no apego, pois o que pode esse
homem fazer por outro? Esse é o terceiro mestre que é censurável ... E esses
são os três tipos de mestres no mundo que são censuráveis.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>19. Então
Lohicca disse: “Venerável Gotama, há mestres no mundo que não são censuráveis?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>20-55. “É o
caso, Lohicca, em que um Tathagata surge no mundo, um arahant,
perfeitamente iluminado, consumado no verdadeiro conhecimento e conduta,
bem-aventurado, conhecedor dos mundos, um líder insuperável de pessoas
preparadas para serem treinadas, mestre de devas e humanos, desperto, sublime.
Ele declara - tendo realizado por si próprio com o conhecimento direto - este
mundo com os seus devas, maras e brahmas, esta população com seus contemplativos e
brâmanes, seus príncipes e povo. Ele ensina o Dhamma, com o significado e
fraseado corretos, que é admirável no início, admirável no meio, admirável no
final; e ele revela uma vida santa que é completamente perfeita e imaculada. Um
discípulo segue a vida santa e pratica a virtude, guarda as portas dos
sentidos, alcança o primeiro jhana (<a href="DN2.php#T41">DN 2 versos 41-76</a>).
Sempre que o discípulo de um mestre alcança uma excelente distinção como essa,
esse mestre não deve ser criticado no mundo. E se alguém criticar esse mestre,
essa crítica será inapropriada, falsa, em desacordo com a realidade e
injustificável.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>56-62. “Ele
alcança os outros três jhanas (<a href="DN2.php#T77">DN 2 versos 77-82</a>) ele
realiza vários insights<span style='mso-spacerun:yes'>  </span>(<a
href="DN2.php#T83">DN 2 versos 83-84</a>). Sempre que o discípulo de um mestre
alcança uma excelente distinção como essa, esse mestre não deve ser criticado
no mundo ...<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>63-77. “Ele penetra as Quatro Nobres Verdades, o caminho que conduz à cessação das impurezas<span style='mso-spacerun:yes'>  </span>(<a href="DN2.php#T85">DN 2
versos 85-97</a>). Sempre que o discípulo de um mestre alcança uma excelente
distinção como essa, esse mestre não deve ser criticado no mundo. E se alguém
criticar esse mestre, essa crítica será inapropriada, falsa, em desacordo com a
realidade e injustificável.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>78. Com
base nisso o Brâmane Lohicca disse: “Venerável Gotama, é como se um homem
agarrasse pelo cabelo alguém que tendo tropeçado estivesse caindo num abismo, [<a
href="#N3">3</a>] <a name=R3></a><span style='mso-spacerun:yes'> </span>eu
também estava caindo num abismo e fui resgatado pelo Venerável Gotama.
Magnífico, Venerável Gotama! Magnífico, Venerável Gotama! Venerável Gotama
esclareceu o Dhamma de várias formas, como se tivesse colocado em pé o que
estava de cabeça para baixo, revelasse o que estava escondido, mostrasse o
caminho para alguém que estivesse perdido ou segurasse uma lâmpada no escuro
para aqueles que possuem visão pudessem ver as formas. Eu busco refúgio no
Venerável Gotama, no Dhamma e na Sangha dos bhikkhus. Que o Venerável Gotama me
aceite como discípulo leigo que buscou refúgio para o resto da vida!”<o:p></o:p></span></p>

