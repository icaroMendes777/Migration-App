<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. Então o
Abençoado lhe disse: &quot;Aquilo que você descreveu como desenvolvimento do
corpo, Aggivessana, não é o desenvolvimento do corpo de acordo com o Dhamma na
