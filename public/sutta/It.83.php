<p class=Normal> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: "Bhikkhus, quando um deva irá falecer do grupo dos devas, surgem cinco sinais de presságio: a sua grinalda de flores murcha, as suas vestes ficam sujas, suor sai das axilas, o brilho do corpo desvanece, e o deva não se delicia no paraíso.
Os devas, vendo os sinais de presságio que aquele deva irá falecer, encorajam-no com três coisas, com as palavras: 'Vá daqui, amigo, para um bom destino. Tendo ido para um bom destino obtenha aquilo que é bom de ser obtido. Tendo obtido aquilo que é bom de ser obtido, estabeleça-se firmemente nisso.'"</p>

<p class=Normal> Quando isso foi dito, um certo bhikkhu perguntou ao Abençoado: "Venerável senhor, o que é considerado pelos devas como um bom destino? O que é considerado pelos devas como um ganho bom de ser obtido? O que é considerado pelos devas como estabelecer-se firmemente?" </p>

<p class=Normal><a name=R1></a> "É a existência humana, bhikkhu, que é considerada pelos devas como um bom destino. Quando um ser humano obtém fé no Dhamma e Disciplina ensinados pelo Tathagata, isso é considerado pelos devas como um ganho bom de ser obtido. Quando a fé está estável, firme, estabelecida e forte, e não será destruída [<a href="#N1">1</a>] por algum contemplativo ou brâmane, ou deva, ou Mara, ou Brahma, ou por qualquer outro no mundo, isso é considerado pelos devas como estabelecido firmemente."</p>

<p class=Normal style='margin-left:.75in'> Quando um deva cujo tempo de vida se extinguiu<br>
está para falecer da companhia dos devas, <br>
os devas o encorajam<br>
de três modos com estas palavras:</p>

<p class=Normal style='margin-left:.75in'> "Vá, amigo, para um bom destino, <br>
para a companhia dos humanos. <br>
Tornando-se um humano desenvolva a fé<br>
insuperável no verdadeiro Dhamma.</p>

<p class=Normal style='margin-left:.75in'> Essa fé estável, <br>
firme e forte, <br>
durante a vida será inabalável<br>
no verdadeiro Dhamma bem proclamado.</p>

<p class=Normal style='margin-left:.75in'> Tendo abandonado a conduta imprópria com o corpo, <br>
a conduta imprópria com a linguagem, <br>
a conduta imprópria com a mente, <br>
que são consideradas uma imperfeição.</p>

<p class=Normal style='margin-left:.75in'> Tendo feito muito daquilo que é bom<br>
ambos através do corpo e linguagem, <br>
e através da mente feito o bem <br>
que é ilimitado e livre do apego.</p>

<p class=Normal style='margin-left:.75in'> Tendo esse mérito como base, <br>
feito abundante através da generosidade, <br>
você deveria estabelecer outras pessoas<br>
no verdadeiro Dhamma e na vida santa."</p>

<p class=Normal style='margin-left:.75in'> Quando os devas sabem <br>que um deva
irá falecer do seu grupo,<br>
por compaixão eles o encorajam: <br>
"Deva, volte mais vezes para cá."</p>


