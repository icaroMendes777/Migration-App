<p class=Normal>Assim ouvi. Em certa ocasião o Abençoado estava em Rajagaha, na montanha do Pico do Abutre. Então o errante Sutavan foi até o Abençoado e ao chegar o cumprimentou. Depois da troca de saudações amigáveis e corteses ele sentou a um lado e disse: </p>

 

<p class=Normal> “Certo dia, recentemente, enquanto eu estava aqui mesmo em Rajagaha, em Giribbaja, eu ouvi na presença do Abençoado, aprendi na presença do Abençoado o seguinte: ‘Sutavan, um bhikkhu que é um Arahant, que destruiu as impurezas, viveu a vida santa, fez o que devia ser feito, depôs o fardo, alcançou o verdadeiro objetivo, destruiu os grilhões da existência e está completamente libertado através do conhecimento supremo, não é capaz de transgredir esses cinco princípios. É impossível que um bhikkhu, cujas impurezas mentais terminaram, tirar, de modo intencional, a vida de um ser vivo. É impossível que um bhikkhu, cujas impurezas mentais terminaram, tomar, como se fosse um roubo, aquilo que não foi dado. É impossível que um bhikkhu, cujas impurezas mentais terminaram, praticar o ato sexual. É impossível que um bhikkhu, cujas impurezas mentais terminaram, deliberadamente dizer uma mentira. É impossível que um bhikkhu, cujas impurezas mentais terminaram, armazenar coisas de modo a desfrutar de prazeres dos sentidos tais como ele fazia quando era leigo.’ Venerável senhor, eu ouvi corretamente, prestei atenção corretamente, entendi corretamente, lembrei corretamente?” </p>

 

<p class=Normal> “Sim, Sutavan, você ouviu corretamente, prestou atenção corretamente, entendeu corretamente, lembrou corretamente, e também agora eu digo isso: 'Um bhikkhu que é um Arahant, com as impurezas destruídas, que viveu a vida santa, fez o que devia ser feito, depôs o fardo, alcançou o verdadeiro objetivo, destruiu os grilhões da existência e está completamente libertado através do conhecimento supremo, é incapaz de transgredir em nove casos. </p>

 

<p class=Normal> “[1] É impossível que um bhikkhu, cujas impurezas mentais terminaram, tirar, de modo intencional, a vida de um ser vivo. [2] É impossível que um bhikkhu, cujas impurezas mentais terminaram, tomar, como se fosse um roubo, aquilo que não foi dado. [3] É impossível que um bhikkhu, cujas impurezas mentais terminaram, praticar o ato sexual. [4] É impossível que um bhikkhu, cujas impurezas mentais terminaram, deliberadamente dizer uma mentira. [5] É impossível que um bhikkhu, cujas impurezas mentais terminaram, armazenar coisas de modo a desfrutar de prazeres dos sentidos tais como ele fazia quando era leigo. </p>

 

<p class=Normal> “[6] É impossível que um bhikkhu, cujas impurezas mentais terminaram, pratique uma ação baseada no desejo. [7] É impossível que um bhikkhu, cujas impurezas mentais terminaram, pratique uma ação baseada na raiva. [8] É impossível que um bhikkhu, cujas impurezas mentais terminaram, pratique uma ação baseada no medo. [9] É impossível que um bhikkhu, cujas impurezas mentais terminaram, pratique uma ação baseada na delusão. </p>

 

<p class=Normal> “Tanto antes como agora eu lhe digo que um bhikkhu que é um Arahant, com as impurezas destruídas, que viveu a vida santa, fez o que devia ser feito, depôs o fardo, alcançou o verdadeiro objetivo, destruiu os grilhões da existência e está completamente libertado através do conhecimento supremo, é incapaz de transgredir nesses nove casos.” </p>

