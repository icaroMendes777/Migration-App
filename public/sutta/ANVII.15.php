<p class=Normal> “Bhikkhus, há sete tipos de pessoas que podem ser encontradas no mundo. Quais sete? </p>

 

<p class=Normal> “Há o caso em que um indivíduo afunda e permanece afundado. Há o caso em que um indivíduo, vem para a superfície, mas afunda novamente. Há o caso em que um indivíduo, vem para a superfície, e ali permanece. Há o caso em que um indivíduo, vem para a superfície, abre os olhos e vê à sua volta. Há o caso em que um indivíduo, vem para a superfície, e sai nadando. Há o caso em que um indivíduo, vem para a superfície, e consegue um apoio. Há o caso em que um indivíduo, vem para a superfície, atravessa, alcança a outra margem, seguro em terra firme, um brâmane. [<a href="#N1">1</a>] </p>

 

<p class=Normal><a name="R1"> </a> “E como um indivíduo afunda e permanece afundado? É o caso em que um indivíduo está dotado exclusivamente com qualidades prejudiciais, inábeis. Assim é como um indivíduo afunda e permanece afundado. [<a href="#N2">2</a>] </p>

 

<p class=Normal><a name="R2"> </a> “E como um indivíduo, vem para a superfície, mas afunda novamente? É o caso em que um indivíduo, vem para a superfície, [e vê] ‘A convicção nas qualidades hábeis é benéfica, a vergonha de cometer transgressões é benéfica, o temor de cometer transgressões é benéfico, a energia é benéfica, a atenção plena é benéfica, a concentração é benéfica, a sabedoria com relação às qualidades hábeis é benéfica.’ Mas a sua convicção nem permanece e tampouco se desenvolve, mas simplesmente declina. A sua vergonha ... , o seu temor ... , a sua energia ... , a sua atenção plena ... , a sua concentração ... , a sua sabedoria nem permanece e tampouco se desenvolve, mas simplesmente declina. Assim é como um indivíduo, vem para a superfície, mas afunda novamente. [<a href="#N3">3</a>] </p>

 

<p class=Normal><a name="R3"> </a> “E como um indivíduo, vem para a superfície, e ali permanece? É o caso em que um indivíduo, vem para a superfície, [e vê] ‘A convicção nas qualidades hábeis é benéfica, a vergonha de cometer transgressões é benéfica, o temor de cometer transgressões é benéfico, a energia é benéfica, a atenção plena é benéfica, a concentração é benéfica, a sabedoria com relação às qualidades hábeis é benéfica.’ A sua convicção não declina, mas ao invés disso permanece e se desenvolve.  A sua vergonha ... , o seu temor ... , a sua energia ... , a sua atenção plena ... , a sua concentração ... , a sua sabedoria não declina, mas ao invés disso permanece e se desenvolve. Assim é como um indivíduo, vem para a superfície, e ali permanece. [<a href="#N4">4</a>] </p>

 

<p class=Normal><a name="R4"> </a> “E como um indivíduo, vem para a superfície, abre os olhos e vê à sua volta? É o caso em que um indivíduo, vem para a superfície, [e vê] ‘A convicção nas qualidades hábeis é benéfica, a vergonha de cometer transgressões é benéfica, o temor de cometer transgressões é benéfico, a energia é benéfica, a atenção plena é benéfica, a concentração é benéfica, a sabedoria com relação às qualidades hábeis é benéfica.’ Com a destruição de três grilhões, ele se tornou aquele que entrou na correnteza, não mais destinado aos mundos inferiores, com o destino fixo, ele tem a iluminação como destino. Assim é como um indivíduo, vem para a superfície, abre os olhos e vê à sua volta. [<a href="#N5">5</a>] </p>

 

<p class=Normal><a name="R5"> </a> “E como um indivíduo, vem para a superfície, e sai nadando? É o caso em que um indivíduo, vem para a superfície, [e vê] ‘A convicção nas qualidades hábeis é benéfica, a vergonha de cometer transgressões é benéfica, o temor de cometer transgressões é benéfico, a energia é benéfica, a atenção plena é benéfica, a concentração é benéfica, a sabedoria com relação às qualidades hábeis é benéfica.’ Com a destruição de três grilhões e com a atenuação da cobiça, raiva e delusão, ele se tornou aquele que retorna uma vez, retornando uma vez a este mundo para dar um fim ao sofrimento. Assim é como um indivíduo, vem para a superfície, e sai nadando. [<a href="#N6">6</a>] </p>

 

<p class=Normal><a name="R6"> </a> “E como um indivíduo, vem para a superfície, e consegue um apoio? É o caso em que um indivíduo, vem para a superfície, [e vê] ‘A convicção nas qualidades hábeis é benéfica, a vergonha de cometer transgressões é benéfica, o temor de cometer transgressões é benéfico, a energia é benéfica, a atenção plena é benéfica, a concentração é benéfica, a sabedoria com relação às qualidades hábeis é benéfica.’ Com a destruição dos cinco primeiros grilhões, ele reaparece espontaneamente [nas Moradas Puras] e lá irá realizar o parinibbana sem nunca mais retornar daquele mundo. Assim é como um indivíduo, vem para a superfície, e consegue um apoio. [<a href="#N7">7</a>] </p>

 

<p class=Normal><a name="R7"> </a> “E como um indivíduo, vem para a superfície, atravessa, alcança a outra margem, seguro em terra firme, um brâmane? É o caso em que um indivíduo, vem para a superfície, [e vê] ‘A convicção nas qualidades hábeis é benéfica, a vergonha de cometer transgressões é benéfica, o temor de cometer transgressões é benéfico, a energia é benéfica, a atenção plena é benéfica, a concentração é benéfica, a sabedoria com relação às qualidades hábeis é benéfica.’ Realizando por si mesmo através do conhecimento direto, aqui e agora, ele entra e permanece na libertação da mente e libertação através da sabedoria que são imaculadas com a destruição de todas as impurezas. Assim é como um indivíduo, vem para a superfície, atravessa, alcança a outra margem, seguro em terra firme, um brâmane. </p>

 

<p class=Normal> “Essas são os sete tipos de pessoas que podem ser encontradas no mundo.” </p>

 

