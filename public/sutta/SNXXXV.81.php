<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então um
grande número de bhikkhus foram até o Abençoado e depois de cumprimentá-lo sentaram
a um lado e disseram: “Aqui, venerável senhor, errantes de outras seitas nos
perguntam: ‘Por qual propósito, amigos, a vida santa é vivida sob o
contemplativo Gotama?’ Quando somos assim perguntados, venerável senhor, nós
respondemos aos errantes da seguinte forma: ‘Amigos, é para a completa
compreensão do sofrimento que a vida santa é vivida sob o Abençoado.’ Venerável
senhor, esperamos que ao responder dessa forma, tenhamos dito aquilo que foi
dito pelo Abençoado e que não o tenhamos deturpado com algo contrário aos
fatos. Esperamos ter explicado de acordo com o Dhamma, de tal modo que nada que
dê margem à censura possa de forma legítima ser deduzido da nossa declaração.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Com
certeza, bhikkhus, ao responderem dessa forma, vocês dizem aquilo que foi dito
por mim e não me deturpam com algo contrário aos fatos; vocês explicam de
acordo com o Dhamma, e não há nada que possa de forma legítima ser deduzido da
sua declaração que dê margem à censura. Pois, bhikkhus, é para a completa
compreensão do sofrimento que a vida santa é vivida sob mim. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas,
bhikkhus, se os errantes de outras seitas perguntarem a vocês: ‘O que, amigos,
é esse sofrimento para cuja completa compreensão a vida santa é vivida sob o
contemplativo Gotama?’ – sendo assim perguntados, vocês deveriam responder-lhes
da seguinte forma: ‘O olho, amigos, é sofrimento: é para a completa compreensão
disso que a vida santa é vivida sob o Abençoado. As formas são sofrimento ... a consciência no olho é
sofrimento ... o contato no olho é sofrimento ...
qualquer sensação que surja tendo o contato no olho como condição - experimentada como prazer, dor ou nem prazer, nem dor - isso
também é sofrimento: é para a completa compreensão
disso que a vida santa é vivida sob o Abençoado. O ouvido é sofrimento ... A mente é sofrimento ... qualquer sensação que surja
tendo o contato na mente como condição - experimentada como prazer, dor ou nem prazer, nem dor - isso também é sofrimento: é para a
completa compreensão disso que a vida santa é vivida sob o Abençoado. Esse,
amigos, é aquele sofrimento para cuja completa compreensão a vida santa é
vivida sob o Abençoado.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sendo
assim perguntados, bhikkhus, vocês deveriam responder a esses errantes de
outras seitas dessa forma.”<o:p></o:p></span></p>


