<p class=Tit1 align=center style='text-align:center'> Sangarava Sutta </Tit1>
<p class=Tit1 align=center style='text-align:center'>Para Sangarava</Tit1>

<p class=Normal align=center style='text-align:center'><span lang=PT-BR style='font-size:
