<p class=Normal> “Bhikkhus, há esses cinco tipos de grilhões da mente. Quais cinco? </p>


<p class=Normal> (1) “Aqui, um bhikkhu não está livre da paixão, desejo, afeição, sede, cobiça e ambição pelos prazeres dos sentidos, e dessa forma a mente dele não se inclina pelo ardor, devoção, perseverança e esforço. Como a mente dele não se inclina pelo ardor, devoção, perseverança e esforço, esse é o primeiro grilhão na mente que ele não partiu. </p>


<p class=Normal> (2) - (5) "Outra vez, um bhikkhu não está está livre da paixão, desejo, afeição, sede, cobiça e ambição pelo corpo, ... não está livre da paixão, desejo, afeição, sede, cobiça e ambição pela forma ... come tanto quanto ele quiser, até que a sua barriga esteja cheia e se entrega aos prazeres da indolência, cochilo e sono ... ele vive a vida santa aspirando por algum plano divino assim: ‘Por esta virtude, ou observância, ou ascetismo, ou vida santa, eu me tornarei um [grande] deva ou algum deva [menor], e desse modo a mente dele não se inclina pelo ardor, devoção, perseverança e esforço. Como a mente dele não se inclina pelo ardor, devoção, perseverança e esforço, esse é o quinto grilhão na mente que ele não partiu. </p>


<p class=Normal>"Esses são os cinco tipos de grilhões na mente ... Esses quatro fundamentos da atenção plena devem ser desenvolvidos para o abandono desses cinco tipos de grilhões na mente."
 </p>

