<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim ouvi.
Em certa ocasião, o Venerável Ananda e o Venerável Bhadda estavam em
Pataliputta no Parque do Galo. Então, ao anoitecer, o Venerável Bhadda saiu do
seu isolamento e foi até o Venerável Ananda e ambos se cumprimentaram. Quando a
conversa cortês e amigável havia terminado, o Venerável Bhadda sentou a um lado
e disse para o Venerável Ananda:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigo
Ananda, qual é a causa e razão porque o verdadeiro Dhamma não perdura depois
que um <a name=PVW>Tathagata</a> tenha realizado o parinibbana? E qual é a
causa e razão porque o verdadeiro Dhamma perdura depois que um Tathagata tenha
realizado o parinibbana?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Muito bem,
amigo Bhadda! A sua inteligência é excelente, a sua perspicácia é excelente, a
sua pergunta é muito boa. Pois você me perguntou:<span
style='mso-spacerun:yes'>  </span>‘Amigo Ananda, qual é a causa e razão porque
o verdadeiro Dhamma não perdura depois que um Tathagata tenha realizado o
parinibbana? E qual é a causa e razão porque o verdadeiro Dhamma perdura
depois que um Tathagata tenha realizado o parinibbana?’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
amigo.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigo, é
porque esses quatro fundamentos da atenção plena não são desenvolvidos e
cultivados que o verdadeiro Dhamma não perdura depois que um Tathagata tenha
realizado o parinibbana. E é porque esses quatro fundamentos da atenção plena
são desenvolvidos e cultivados que o verdadeiro Dhamma perdura depois que um
Tathagata tenha realizado o parinibbana. Quais quatro? Neste caso, amigo, um
bhikkhu permanece contemplando o corpo como um corpo, ardente, plenamente
consciente e com atenção plena, tendo colocado de lado a cobiça e o desprazer
pelo mundo. Ele permanece contemplando as sensações como sensações ... mente
como mente ... objetos mentais como objetos mentais, ardente, plenamente
consciente e com atenção plena, tendo colocado de lado a cobiça e o desprazer
pelo mundo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Porque
esses quatro fundamentos da atenção plena não são desenvolvidos e cultivados é que
o verdadeiro Dhamma não perdura depois que um Tathagata tenha realizado o
parinibbana. E é porque esses quatro fundamentos da atenção plena são
desenvolvidos e cultivados que o verdadeiro Dhamma perdura depois que um
Tathagata tenha realizado o parinibbana.” <o:p></o:p></span></p>


