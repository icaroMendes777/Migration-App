<a name=R7></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>O nome que tenho hoje é verdadeiro:<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>eu não
molesto nenhum ser vivo.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:PT-BR'>Embora tenha
vivido no passado como um bandido<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>com o nome
‘Colar de Dedos’,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>arrastado
