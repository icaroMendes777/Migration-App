<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião o Abençoado estava em Savathi, no Bosque de Jeta, no
Parque de Anathapindika. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. Agora,
naquela ocasião havia no reino do rei Pasenadi de Kosala um bandido chamado
Angulimala que era um assassino, com as mãos tingidas de sangue, habituado a
golpes e violência, impiedoso com os seres vivos. Vilarejos, cidades e
distritos haviam sido destruídos por ele. Ele estava constantemente matando
pessoas e usava os dedos delas em um colar. [<a href="#N1">1</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. Então, ao
amanhecer, o Abençoado se vestiu e tomando a sua tigela e manto externo, foi
para Savathi para esmolar alimentos. </span>Depois de haver esmolado em Savathi
e de haver retornado, após a refeição,<span lang=PT-BR style='mso-ansi-language:
PT-BR'> ele arrumou o seu local de descanso e tomando a tigela e o manto
externo saiu pela estrada que levava para onde estava Angulimala. Pastores e
camponeses que passavam vendo que o Abençoado caminhava na direção que levava
para onde estava Angulimala lhe diziam: “Não siga por essa estrada
contemplativo. Nessa estrada se encontra o bandido Angulimala que é um
assassino, com as mãos tingidas de sangue, habituado a golpes e violência,
impiedoso com os seres vivos. Vilarejos, cidades e distritos foram destruídos
por ele. Ele está constantemente matando pessoas e usa os dedos delas em um
colar. Homens em grupos de dez, vinte, trinta e até quarenta seguiram por esta
estrada e assim mesmo foram vítimas de Angulimala”. Quando isso foi dito, o
Abençoado seguiu em silêncio.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Por uma segunda vez...Por
uma terceira vez os pastores e camponeses disseram isso ao Abençoado, mas ainda
assim o Abençoado seguiu em silêncio.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. O bandido Angulimala viu
o Abençoado se aproximando à distância. Quando ele o viu, pensou: “É admirável,
é maravilhoso! Pessoas em grupos de dez, vinte, trinta e até<span
style="mso-spacerun: yes">  </span>quarenta seguiram por esta estrada e assim
mesmo foram minhas vítimas. E agora esse contemplativo vem sozinho, sem
companhia, como se empurrado pela fé. Porque eu não deveria matar esse
contemplativo?” Angulimala então tomou a sua espada e escudo, afivelou o seu
arco e a aljava e seguiu o Abençoado de perto.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. Então o Abençoado
realizou tamanha façanha com os seus poderes supra-humanos que o bandido
Angulimala, embora caminhasse tão rápido quanto pudesse, não conseguia alcançar
o Abençoado que caminhava em seu passo normal. Então o bandido Angulimala
pensou: “É admirável, é maravilhoso! Antes eu conseguia alcançar e agarrar até
mesmo o elefante mais rápido; eu conseguia alcançar e agarrar até mesmo o
cavalo mais rápido; eu conseguia alcançar e agarrar até mesmo a carruagem mais
rápida; eu conseguia alcançar e agarrar até mesmo o gamo mais rápido; mas
agora, embora esteja caminhando o mais rápido que possa, não consigo alcançar
esse contemplativo que está caminhando em seu passo normal!” Ele parou e chamou
o Abençoado: “Pare, contemplativo! Pare, contemplativo!”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu parei,
Angulimala, pare você também.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o bandido Angulimala
pensou: “Esses contemplativos, filhos do Sakya, falam a verdade, afirmam a
verdade; mas embora esse contemplativo ainda esteja caminhando, ele diz: ‘Eu
parei, Angulimala, pare você também.’ E se eu questionasse esse contemplativo.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6.
Então o bandido Angulimala se dirigiu ao Abençoado em versos da seguinte forma: <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Enquanto
caminha, contemplativo, você diz que parou; <br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>mas agora,
quando eu parei, você diz que não parei.<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>Eu lhe
pergunto agora, Ó contemplativo, qual o significado:<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>como pode
ser que você tenha parado e eu não tenha?”<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Angulimala,
eu parei para sempre,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>eu me
abstenho da violência para com os seres vivos;<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>mas você
não tem nenhum refreamento em relação ao que tem vida:<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>essa é a
razão porque eu parei e você não.”<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ó, até que
enfim este contemplativo, um sábio venerado,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>veio para
esta grande floresta por minha razão. [<a href="#N2">2</a>]<br>

<a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Ouvindo os seus versos com o ensinamento do Dhamma,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>eu de fato
renunciarei ao mal para sempre”.<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim
dizendo, o bandido tomou a sua espada e armas<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>e as
arremessou em uma cova num abismo;<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>o bandido
venerou os pés do Abençoado,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>e depois
ali pediu sua admissão na vida santa.<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:PT-BR'>O
Iluminado, o Sábio da Grande Compaixão,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>o Mestre do
mundo com [todos] os seus devas,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>dirigiu-se
a ele com estas palavras, “Venha, bhikkhu”.<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>E assim foi
como ele se tornou um bhikkhu. [<a href="#N3">3</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R3></a> 7</span><span
lang=PT-BR style='mso-ansi-language:PT-BR'>. Então o Abençoado iniciou a
caminhada de regresso a Savathi com Angulimala como seu acompanhante. Caminhando
em etapas eles acabaram por chegar em Savathi e lá se estabeleceram no Bosque
de Jeta, no Parque de Anathapindika.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8.
Agora naquela ocasião uma grande multidão havia se aglomerado nos portões do
palácio do rei Pasenadi, barulhenta e ruidosa, gritando: “Senhor, o bandido
Angulimala encontra-se no seu reino; ele é um assassino, com as mãos tingidas
de sangue, habituado a golpes e violência, impiedoso com os seres vivos.
Vilarejos, cidades e distritos foram destruídos por ele. Ele está constantemente
matando pessoas e usa os dedos delas em um colar! O rei precisa acabar com
ele!” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9.
Então no meio do dia o rei Pasenadi de Kosala saiu de Savathi com um grupo de
500 cavaleiros em direção ao parque. Ele foi até onde a estrada permitia ir com
a sua carruagem e depois desmontou e seguiu a pé até onde estava o Abençoado.
Depois de cumprimentar o Abençoado ele sentou a um lado e o Abençoado lhe
disse: “O que há, grande rei? O rei Seniya Bimbisara de Magadha o estará
atacando ou os Licchavis de Vesali ou outros reis hostis?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10.
“Venerável senhor, o rei Seniya Bimbisara de Magadha ou os Licchavis de Vesali
ou outros reis hostis não estão me atacando. Mas há um bandido no meu reino
chamado Angulimala, ele é um assassino, com as mãos tingidas de sangue,
habituado a golpes e violência, impiedoso com os seres vivos. Vilarejos,
cidades e distritos foram destruídos por ele. Ele está constantemente matando
pessoas e usa os dedos delas em um colar. Eu nunca serei capaz de acabar com
ele, venerável senhor.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. “Grande rei, suponha
que você visse que Angulimala raspou o seu cabelo e barba, vestiu o manto de
cor ocre e seguiu a vida santa; que ele está se abstendo de matar seres vivos,
de tomar aquilo que não é dado e da linguagem mentirosa; que ele se abstém de comer
à noite, come somente uma vez ao dia, é celibatário, virtuoso, com bom caráter.
Se você o visse assim, como o trataria?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável senhor, nós o
homenagearíamos, ou nos levantaríamos, ou o convidaríamos para que ele se
sentasse; ou o convidaríamos para que aceitasse mantos, alimentos, moradia ou
medicamentos; ou nós lhe proveríamos guarda, defesa e proteção sob a lei. Mas,
venerável senhor, ele é um homem sem moral, mau caráter. Como poderia ter tal
virtude e contenção?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. Agora, naquela ocasião
o venerável Angulimala estava sentado não muito distante do Abençoado. Então o
Abençoado estendeu o seu braço direito e disse ao rei Pasenadi de Kosala:
“Grande rei, este é Angulimala.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o rei Pasenadi ficou
com medo, alarmado e aterrorizado. Sabendo disso, o Abençoado lhe disse: “Não
tema, grande rei, não tema. Não há nada a temer da parte dele.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o medo, alarme e
terror do rei diminuiram. Ele foi até o venerável Angulimala e lhe disse:
“Venerável senhor, você é realmente Angulimala?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim, grande rei.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável senhor, de que
família é o seu pai? De que família é a sua mãe?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Meu pai é um Gagga, grande
rei; minha mãe é uma Mantani.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Que o nobre senhor Gagga
Mantaniputta descanse satisfeito. Eu irei prover mantos, alimentos, moradia e medicamentos
para o nobre senhor Gagga Mantaniputta.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. Agora naquela ocasião o
venerável Angulimala vivia na floresta, esmolava alimentos, vestia mantos
feitos com trapos e se restringia a três mantos. Ele respondeu; “Não é
necessário, grande rei, meus três mantos estão completos.’ <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O rei Pasenadi então voltou
para o Abençoado e após cumprimentá-lo, sentou a um lado e disse: “É admirável,
venerável senhor, é maravilhoso como o Abençoado doma os indomados, traz paz
para os perturbados e conduz a Nibbana aqueles que ainda não realizaram
Nibbana. Venerável senhor, nós mesmos não pudemos domá-lo com a força e armas e
no<span style="mso-spacerun: yes">  </span>entanto o Abençoado o domou sem
força e sem armas. E agora, venerável senhor, nós partiremos. Estamos muito
ocupados e temos muito que fazer.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Agora é o momento, grande
rei, faça como julgar adequado.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o rei Pasenadi de
Kosala levantou do seu assento e depois de homenagear o Abençoado, mantendo-o à
sua direita, partiu.</span><span lang=PT-BR style='font-size:11.0pt;mso-bidi-font-size:
12.0pt;mso-ansi-language:PT-BR'><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14. Então, ao amanhecer, o
venerável Angulimala se vestiu e tomando a sua tigela e manto externo, foi para
Savathi esmolar alimentos. Enquanto ele perambulava de casa em casa em Savathi,
ele viu uma certa mulher dando a luz a uma criança deformada. Vendo isso, ele
pensou: “Como os seres sofrem! De fato, como os seres sofrem!”<o:p></o:p></span></p>

<p class=Normal>Depois de haver esmolado em Savathi e de haver retornado, após a refeição
ele foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse<span
lang=PT-BR style='mso-ansi-language:PT-BR'>: “Venerável senhor, pela manhã me
vesti e tomando a minha tigela e manto externo, fui para Savathi para esmolar
alimentos. Enquanto perambulava de casa em casa em Savathi, vi uma certa mulher
dar à luz a uma criança defeituosa. Vendo isso, pensei: “Como os seres sofrem!
De fato, como os seres sofrem!”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. “Nesse caso,
Angulimala, vá para Savathi e diga<a name=R9></a> para aquela mulher: “Irmã,
desde que nasci, não me recordo de intencionalmente haver privado da vida nenhum
ser vivo. Por essa verdade, que você e a sua criança fiquem bem!” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável senhor, não
estaria eu contando uma mentira deliberada, pois intencionalmente privei da
vida muitos seres vivos?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então, Agulimala, vá para
Savathi e diga para aquela mulher: “Irmã, desde que nasci com o nobre
nascimento, não me recordo de intencionalmente haver privado da vida nenhum ser
vivo. Por essa verdade, que você e a sua criança fiquem bem!” [<a href="#N4">4</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R4></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
venerável senhor”, o venerável Angulimala respondeu e tendo ido até Savathi
disse para aquela mulher: “Irmã, desde que nasci com o nobre nascimento, não
me recordo de intencionalmente haver privado da vida nenhum ser vivo. Por essa
verdade, que você e a sua criança fiquem bem!” Então a mulher e a criança
melhoraram.<o:p></o:p></span></p>

<p class=Normal><a name=R12></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>16. Depois
de não muito tempo, permanecendo só, isolado, diligente, ardente e decidido, o
venerável Angulimala, </span>em pouco tempo, alcançou e permaneceu no objetivo
supremo da vida santa pelo qual membros de um clã deixam a vida em família pela
vida santa, tendo conhecido e realizado por si mesmo no aqui e agora. Ele
soube: “O nascimento foi destruído, a vida santa foi vivida, o que deveria ser
feito foi feito, não há mais vir a ser a nenhum estado.”<span lang=PT-BR
style='mso-ansi-language:PT-BR'> E assim o venerável Angulimala </span>tornou-se
mais um dos Arahants<span lang=PT-BR style='mso-ansi-language:PT-BR'>. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17. Então, ao amanhecer, o
venerável Angulimala se vestiu e tomando a tigela e manto externo, foi para
Savathi para esmolar alimentos. Agora naquela ocasião alguém jogou uma pedra e
atingiu o corpo do venerável Angulimala, outra pessoa jogou um pau e atingiu o
corpo dele e outra pessoa jogou um pedaço de cerâmica e atingiu o corpo dele.
Então, com o sangue jorrando da sua cabeça cortada, com a sua tigela quebrada e
com o seu manto externo rasgado, o venerável Angulimala foi até o Abençoado. O
Abençoado o viu chegando à distância e lhe disse: “Agüente, brâmane! Agüente,
brâmane! Você está<span style="mso-spacerun: yes">  </span>experimentando aqui
e agora o resultado de ações pelas quais você poderia ser torturado no inferno
durante muitos anos, por muitas centenas de anos, por muitos milhares de
anos.”[</span><a href="#N5">5</a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>]<o:p></o:p></span></p>

<p class=Normal><a name=R13></a><a name=R5></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>18. Então, enquanto o venerável Angulimala estava sozinho em retiro,
experimentando o prazer da libertação, ele pronunciou o seguinte: [<a href="#N6">6</a>] <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><a name=R6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Quem antes vivia em negligência <br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>e depois
não é mais negligente,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>ilumina o
mundo<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>tal como a
lua liberta das nuvens.<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quem
inspeciona as más ações que cometeu<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>praticando
ações benéficas no seu lugar,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>ilumina o
mundo<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>tal como a
lua liberta das nuvens.<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:PT-BR'>O jovem
bhikkhu que dedica<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>o seu
esforço aos ensinamentos do Buda,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>ilumina o
mundo<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>tal como a
lua liberta das nuvens.<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:PT-BR'>Que meus
inimigos ouçam um discurso do Dhamma,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>que eles se
dediquem aos ensinamentos do Buda,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>que meus
inimigos cuidem dessas pessoas de bem<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>que
conduzem outras a aceitarem o Dhamma.<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:PT-BR'>Que meus
inimigos prestem atenção ocasionalmente<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>e ouçam o
Dhamma daqueles que pregam a tolerância,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>daqueles
que também falam em favor da bondade,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>e que eles
sigam esse Dhamma com ações bondosas.<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:PT-BR'>Pois então
com certeza, eles não irão desejar causar dano a mim,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>nem
pensarão em causar dano a outros seres,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>portanto,
aqueles que protegem a todos, fracos ou fortes,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>que eles
alcancem a paz insuperável.<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:PT-BR'>Aqueles que
fazem canais, conduzem a água,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>arqueiros
retificam as flechas,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>carpinteiros
retificam a madeira,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>mas os
homens sábios buscam domar a si mesmos.<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:PT-BR'>Existem
alguns que são domados com surras,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>alguns com
grilhões e alguns com chicotes;<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>mas eu fui
domado por alguém só,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>que não
possui vara ou arma.<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:PT-BR'>‘Inofensivo’
é o nome que tenho,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>embora eu
tenha sido perigoso no passado. [<a href="#N7">7</a>]<span
