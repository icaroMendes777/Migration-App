<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] O nome “Angulimala” é um epíteto que significa “colar <i>(mala) </i>de
dedos <i>(anguli)”.</i> Ele era o filho do brâmane Bhaggava, um capelão do rei
Pasenadi de Kosala. O seu nome de nascimento era Ahimsaka que significa “o
inofensivo”. Ele estudou em Takkasila onde ele se tornou o favorito do seu
mestre. Os seus colegas estudantes, com inveja dele, disseram ao mestre que
Ahimsaka havia cometido adultério com a sua esposa. O mestre com a intenção de
arruiná-lo ordenou que ele lhe trouxesse mil dedos humanos da mão direita a
título de honorários. Ahimsaka vivia na floresta Jalini, atacando viajantes e
cortando um dedo de cada um e usando-os como um colar. Na abertura do sutta lhe
faltava apenas um dedo para completar os mil e ele estava determinado a matar a
primeira pessoa que aparecesse. O Buda viu que a mãe de Angulimala estava a caminho
para visitá-lo e tendo consciência de que Angulimala tinha as condições
necessárias para alcançar o estado de arahant, ele o encontrou pouco tempo
antes que a sua mãe chegasse. [<a href="#R1">Retorna</a>]<o:p></o:p></span></p>

<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[2] MA
explica que Angulimala havia se dado conta de que o monge à sua frente era o
próprio Buda e que ele havia vindo para a floresta com o propósito específico
de transformá-lo. [<a href="#R2">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N3></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[3] MA: Por
virtude de seus méritos de vidas passadas, Angulimala obteve a tigela e os
mantos através dos poderes supra-humanos do Buda no momento em que ele disse
“Venha, bhikkhu”. [<a href="#R3">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N4></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[4] Mesmo
hoje esta declaração é freqüentemente recitada por monges Budistas como um
encanto protetor, <i>(paritta)</i>, para mulheres grávidas que estão próximas do
parto. [<a href="#R4">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N5></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[5] MA
explica que toda ação volitiva, <i>(kamma)</i>, pode produzir três tipos de
resultados: um resultado para ser experimentado aqui e agora, isto é, na mesma
vida em que a ação é cometida; um resultado para ser experimentado na próxima
existência; e um resultado para ser experimentado em qualquer existência
subseqüente à próxima, contanto que haja continuidade na permanência no
samsara. Como ele havia alcançado o estado de arahant, Angulimala havia
escapado dos dois últimos tipos de resultados mas não do primeiro, já que mesmo
os arahants estão sujeitos a experimentar os resultados nesta existência de
ações realizadas antes de atingir o estado de arahant.[<a href="#R5">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal> <a name=N6></a><span lang=PT-BR style='mso-ansi-language:
