<p class=Normal> "Bhikkhus, antes da minha iluminação, quando eu ainda era apenas um Bodisatva não iluminado, eu pensei: (1) 'Qual é a gratificação no mundo? (2) Qual é o seu perigo? (3) Qual é a sua escapatória?' [<a href="#N1">1</a>] </p>

<p class=Normal><a name=R1></a>"Bhikkhus, então me ocorreu: 'O prazer e a alegria que surgem na dependência do mundo: essa é a gratificação do mundo. Esse mundo é impermanente, sofrimento e sujeito à mudança: esse é o perigo no mundo. A remoção e o abandono do desejo e cobiça pelo mundo: essa é a escapatória do mundo.'</p>

<p class=Normal> “Enquanto, bhikkhus, não obtive o conhecimento direto de como na verdade são a gratificação, o perigo e a escapatória no caso do mundo, eu não reivindiquei ter despertado para a insuperável perfeita iluminação neste mundo com os seus devas, maras e brahmas, esta população com seus contemplativos e brâmanes, seus príncipes e povo. Mas quando obtive o conhecimento direto de como na verdade são a gratificação, o perigo e a escapatória no caso do mundo, então reivindiquei ter despertado para a insuperável perfeita iluminação neste mundo com ... seus príncipes e povo.</p>

<p class=Normal> “O conhecimento e visão surgiram em mim: ‘Inabalável é a libertação da minha mente; este é o meu último nascimento; agora não há mais vir a ser a nenhum estado.’”</p>

