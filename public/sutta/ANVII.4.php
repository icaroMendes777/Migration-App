<p class=Normal> “Bhikkhus, há esses sete poderes. Quais sete? O poder da convicção, o poder da energia, o poder da vergonha de cometer transgressões, o poder do temor de cometer transgressões, o poder da atenção plena, o poder da concentração, e o poder da sabedoria. </p>

<p class=Normal> “E qual é o poder da convicção? É o caso em que um nobre discípulo, tem convicção da iluminação do Tathagata assim: ‘O Abençoado é um arahant, perfeitamente iluminado, consumado no verdadeiro conhecimento e conduta, bem-aventurado, conhecedor dos mundos, um líder insuperável de pessoas preparadas para serem treinadas, mestre de devas e humanos, desperto, sublime.’ Isso é chamado o poder da convicção. </p>

<p class=Normal> “E qual é o poder da energia? É o caso em que um nobre discípulo, permanece com a sua energia estimulada para o abandono de qualidades mentais prejudiciais e para a obtenção de qualidades mentais benéficas. Ele é decidido, firme no seu esforço, sem se esquivar das suas responsabilidades com relação ao cultivo de qualidades mentais benéficas. Isso é chamado o poder da energia. </p>

<p class=Normal><a name=R1></a> “E qual é o poder da vergonha de cometer transgressões? [<a href="#N1">1</a>] É o caso em que um nobre discípulo, tem senso da vergonha; ele sente vergonha da conduta corporal imprópria, da conduta verbal imprópria e da conduta mental imprópria. Isso é chamado o poder da vergonha.

<p class=Normal> “E qual é o poder do temor de cometer transgressões? É o caso em que um nobre discípulo, tem senso do temor; ele teme a conduta corporal imprópria, a conduta verbal imprópria e a conduta mental imprópria. Isso é chamado o poder do temor. </p>

<p class=Normal> “E qual é o poder da atenção plena? É o caso em que um nobre discípulo tem atenção plena, ele possui suprema atenção plena e discriminação, ele se recorda e lembra aquilo que foi feito e dito há muito tempo. Isso é chamado o poder da atenção plena. </p>

<p class=Normal> “E qual é o poder da concentração? É o caso em que um nobre discípulo afastado dos prazeres sensuais, afastado das qualidades não hábeis, entra e permanece no primeiro jhana (igual ao <a href="ANV.14.php">AN V.14</a>) ... entra e permanece no quarto jhana, que possui nem felicidade nem sofrimento, com a atenção plena e a equanimidade purificadas. Isso é chamado o poder da concentração. </p>

<p class=Normal> “E qual é o poder da sabedoria? É o caso em que um nobre discípulo, tem sabedoria, com a completa compreensão da origem e cessação - nobre, penetrante, que conduz ao fim do sofrimento. Isso é chamado o poder da sabedoria. </p>

<p class=Normal> “Esses, bhikkhus, são os sete poderes." </p>

<p class=Normal>O poder da convicção, o poder da energia,<br>
os poderes da vergonha e do temor de cometer transgressões;<br>
os poderes da atenção plena e da concentração,<br>
a sabedoria como sétimo poder;<br>
um bhikkhu poderoso possuindo-os<br>
é sábio e vive feliz. </p>

<p class=Normal>Ele deve examinar o Dhamma com cuidado<br>
e desenvolver o insight e a sabedoria.<br>
Igual a uma vela que se extingue<br>
é a emancipação da mente. </p>


