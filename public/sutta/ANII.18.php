<p class=Normal>Então, o venerável Ananda foi até o Abençoado e depois de cumprimentá-lo sentou a um lado. O Abençoado disse: </p>

<p class=Normal>"Eu digo definitivamente, Ananda, que os atos de conduta corporal imprópria, conduta verbal imprópria, e conduta mental imprópria não devem ser praticados." [<a href="#N1">1</a>]</p>

<p class=Normal><a name=R1></a>"Uma vez que, venerável senhor, o Abençoado declarou definitivamente que os atos de conduta corporal imprópria, conduta verbal imprópria e conduta mental imprópria não devem ser praticados, qual é o perigo que se pode esperar em agir assim?" </p>

<p class=Normal>"Ananda, eu tenho declarado definitivamente que os atos de conduta corporal imprópria, conduta verbal imprópria, e conduta mental imprópria não devem ser praticados porque agindo assim é de se esperar este perigo: a pessoa culpa a si mesma; o sábio, tendo investigado, a censura; ela ganha má reputação; a pessoa morre confusa; e com a dissolução do corpo, após a morte, ela renasce no plano da miséria, em um destino ruim, nos mundos inferiores, no inferno. Eu tenho declarado definitivamente que os atos de conduta verbal imprópria, conduta corporal imprópria e conduta mental imprópria não devem ser praticados  porque agindo assim esse é o perigo que se pode esperar. </p>

<p class=Normal>"Eu digo definitivamente Ananda, que os atos de conduta corporal apropriada, conduta verbal apropriada e conduta e mental apropriada devem ser praticados." </p>

<p class=Normal>"Uma vez que, venerável senhor, o Abençoado declarou definitivamente que os atos de conduta corporal apropriada, conduta verbal apropriada e conduta mental apropriada devem ser praticados, qual é o benefício que se pode esperar em agir assim?" </p>

<p class=Normal>"Ananda, eu tenho declarado definitivamente que os atos de conduta corporal apropriada, conduta verbal apropriada, e conduta mental apropriada devem ser praticados porque agindo assim é de se esperar este benefício: a pessoa não culpa a si mesma; o sábio, tendo investigado, a elogia; ela ganha boa reputação; a pessoa morre sem estar confusa; e com a dissolução do corpo, após a morte, ela renasce num destino feliz, no paraíso. Eu tenho declarado definitivamente que os atos de conduta verbal apropriada, conduta corporal apropriada e conduta mental apropriada devem ser praticados  porque agindo assim esse é o benefício que se pode esperar. </p>

