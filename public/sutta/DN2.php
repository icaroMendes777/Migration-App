Esta é a origem das impurezas ... Esta é a cessação das impurezas ... Este é o
caminho que conduz à cessação das impurezas.’ Ao conhecer e ver, a sua mente
está livre da impureza do desejo sensual, da impureza de ser/existir, da
impureza da ignorância. Quando ela está libertada surge o conhecimento,
‘Libertada.’ Ele compreende que ‘O nascimento foi destruído, a vida santa foi
vivida, o que devia ser feito foi feito, não há mais vir a ser a nenhum
estado.’ </span></p>

<p class=Normal><span lang=PT-BR>“Este, também, grande rei, é um fruto da vida
contemplativa, visível no aqui e agora, mais excelente do que os anteriores e
mais sublime. E quanto a outro fruto visível da vida contemplativa, maior e
mais sublime do que isto, não existe.&quot; </span></p>

<p class=Normal><span lang=PT-BR>99. Quando isto foi dito, o rei Ajatasattu disse ao
Abençoado: “Magnífico, venerável senhor! Magnífico! Como se colocasse em pé o
que estava virado, revelasse o que estava escondido, mostrasse o caminho para
alguém perdido, ou segurasse uma lâmpada no escuro de forma que aqueles que
possuem olhos possam ver as formas, da mesma forma o Abençoado – através de
muitas linhas de argumentação – esclareceu o Dhamma. Eu busco refúgio no
Abençoado, no Dhamma e na comunidade de bhikkhus. Que o Abençoado se lembre de
mim como o discípulo leigo que tomou refúgio deste dia em diante para o resto
da sua vida.</span></p>

<p class=Normal><span lang=PT-BR>100. “Uma transgressão foi cometida por mim, venerável
senhor, em que eu fui tão tolo, tão atrapalhado e tão inábil que matei meu pai
– um homem justo, um rei justo – para obter o domínio do reinado. Que o
Abençoado por favor aceite esta confissão da minha transgressão como tal, de
forma que eu possa me refrear no futuro.” [<a href="#N31">31</a>]</span></p>

<p class=Normal><a name=R31></a><span lang=PT-BR>“Sim, grande rei, uma transgressão foi
cometida por você, em que você foi tão tolo, tão atrapalhado e tão inábil que
matou o seu pai – um homem justo, um rei justo – para obter o domínio do
reinado. Porém, porque você vê a sua transgressão como tal e faz correções de
acordo com o Dhamma, nós aceitamos a sua confissão. Pois é motivo de
crescimento no Dhamma e Disciplina dos nobres quando, vendo uma transgressão
como tal, a pessoa faz correções de acordo com o Dhamma e pratica a contenção
no futuro.&quot; </span></p>

<p class=Normal><span lang=PT-BR>101. Quando isto foi dito, o rei Ajatasattu disse ao
Abençoado: “Bem, então, venerável senhor, eu partirei agora. Muitas são minhas
tarefas, muitas minhas responsabilidades.” </span></p>

<p class=Normal><span lang=PT-BR>“Agora é o momento, grande rei, faça como julgar adequado.”</span></p>

<p class=Normal><span lang=PT-BR>102. Então, o rei Ajatasattu, tendo ficado satisfeito e
contente com as palavras do Abençoado, levantou-se do seu assento, curvou-se
ante o Abençoado e mantendo-o à sua direita partiu. Não muito tempo depois que
o rei Ajatasattu havia partido, o Abençoado dirigiu-se aos bhikkhus: “ O rei
está derrotado, bhikkhus, a sua sorte está selada. [<a href="#N32">32</a>] <a
name=R32></a>Não tivesse ele matado o seu pai – aquele homem justo, aquele rei
justo – o olho do Dhamma, [<a href="#N33">33</a>]<a name=R33></a> límpido,
imaculado teria surgido nele enquanto ele estava sentado neste assento.” </span></p>

<p class=Normal><span lang=PT-BR>Isso foi o que disse o Abençoado. Os bhikkhus ficaram
satisfeitos e contentes com as palavras do Abençoado. </span></p>

