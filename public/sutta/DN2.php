<p class=Normal>&nbsp;</p>

<p class=Normal><span lang=PT-BR>1. Assim ouvi. Em certa ocasião o Abençoado estava em
Rajagaha, no manguezal de Jivaka Komarabhacca [<a href="#N1">1</a>]<a name=R1></a>com
uma grande Sangha de bhikkhus - 1.250 bhikkhus no total. Agora naquela ocasião,
sendo o Uposatha do décimo quinto dia [<a href="#N2">2</a>], <a name=R2></a>a
noite de lua cheia no quarto mês[<a href="#N3">3</a>], <a name=R3></a>chamado
Komudi [<a href="#N4">4</a>] - <a name=R4></a>o rei Ajatasattu Vedehiputta de
Magadha [<a href="#N5">5</a>], <a name=R5></a>estava sentado no terraço no topo
do seu palácio cercado pelos seus ministros. Então ele sentiu inspiração para
exclamar: “Que maravilhosa é esta noite iluminada pela lua! Que bonita ... Que
agradável ... Que inspiradora ... Que auspiciosa é esta noite iluminada pela
lua! Qual brâmane ou contemplativo deveríamos visitar esta noite que possa
iluminar e trazer paz para os nossos corações?” [<a href="#N6">6</a>] </span></p>

<p class=Normal><a name=R6></a><span lang=PT-BR>2. Quando isto foi dito, um dos ministros
disse ao rei: “Majestade, aí está Purana Kassapa, o líder de uma comunidade, o
líder de um grupo, o mestre de um grupo, honrado e famoso, estimado como santo
pelas pessoas. Ele é idoso, adotou a vida santa há muito tempo, com a idade
avançada, na sua última fase da vida. Você deveria visitá-lo. Talvez, se você o
visitasse, ele pudesse iluminar e trazer paz para o seu coração.” Quando isso
foi dito, o rei permaneceu em silêncio.</span></p>

<p class=Normal><span lang=PT-BR>3-7. Então um outro ministro disse ao rei: “Majestade, aí
está Makkhali Gosala ...” “Majestade, aí está Ajita Kesakambala ...” “Majestade,
aí está Pakudha Kaccayana ...” “Majestade, aí está Sañjaya Belatthaputta ...”
“Majestade, aí está Nigantha Nataputta, o líder de uma comunidade, o líder de
um grupo, o mestre de um grupo, honrado e famoso, estimado como santo pelas
pessoas. Ele é idoso, adotou a vida santa há muito tempo, com a idade avançada,
na sua última fase da vida. Você deveria visitá-lo. Talvez, se você o
visitasse, ele pudesse iluminar e trazer paz para o seu coração.”<span
style='mso-spacerun:yes'>  </span>Quando isso foi dito, o rei permaneceu em
silêncio.</span></p>

<p class=Normal><span lang=PT-BR>8. Todo esse tempo Jivaka Komarabhacca estava sentado em
silêncio não muito distante do rei. Assim o rei lhe disse: “Amigo Jivaka, por
que você esta silencioso?” </span></p>

<p class=Normal>“Majestade, aí
está o Abençoado, o Arahant, o Buda perfeitamente iluminado, ele está no meu
manguezal com uma grande Sangha de bhikkhus - 1.250 no total. E acerca desse
mestre Gotama existe essa boa reputação: ‘Esse Abençoado é um arahant,
perfeitamente iluminado, consumado no verdadeiro conhecimento e conduta,
bem-aventurado, conhecedor dos mundos, um líder insuperável de pessoas
preparadas para serem treinadas, [<a href="#N7">7</a>] <a name=R7></a>mestre de devas e humanos, desperto,
sublime.’ Você deveria visitá-lo. Talvez se você o visitasse, ele pudesse
iluminar e trazer paz para 
o seu coração.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR>“Então nesse caso, amigo Jivaka, prepare os elefantes de
montaria.” </span></p>

<p class=Normal><span lang=PT-BR>9. Tendo respondido, “Assim seja, majestade,” e após
preparar quinhentas elefantas bem como o elefante pessoal do rei, Jivaka
anunciou para o rei: “Majestade, os elefantes de montaria estão preparados. Faça
como julgar adequado.” </span></p>

<p class=Normal><span lang=PT-BR>Então o rei, tendo quinhentas das suas mulheres montadas
nas quinhentas elefantas - uma em cada - e tendo montado o seu elefante real,
saiu de Rajagaha com toda a pompa da realeza, com criados carregando tochas, na
direção do manguezal de Jivaka Komarabhacca. </span></p>

<p class=Normal><span lang=PT-BR>10. Porém quando o rei estava não muito distante do
manguezal, ele foi tomado pelo medo, terror, o seu cabelo em pé. Temeroso,
agitado, seu cabelo em pé, ele disse para Jivaka Komarabhacca: “Amigo Jivaka,
você não está me enganando, está? Você não está me traindo, está? Você não está
me entregando para meus inimigos, está? Como pode haver uma comunidade de
bhikkhus tão grande - 1.250 no total - sem nenhum ruído de espirros, nenhum
ruído de tosse, sem qualquer ruído de vozes?” </span></p>

<p class=Normal><span lang=PT-BR>“Não tema, grande rei. Não tema. Eu não o estou enganando
ou traindo ou entregando aos seus inimigos. Vá em frente, grande rei, vá em
frente! Aquelas são lâmpadas ardendo no pavilhão.” </span></p>

<p class=Normal><span lang=PT-BR>11. Então o rei foi até onde a estrada permitia o acesso
dos elefantes e depois desmontou e se aproximou da porta do pavilhão a pé.
Chegando, ele perguntou a Jivaka: “Aonde, amigo Jivaka, está o Abençoado?” </span></p>

<p class=Normal><span lang=PT-BR>“Aquele é o Abençoado, grande rei, sentado contra o pilar
do meio, cercado pela comunidade de bhikkhus.”</span></p>

<p class=Normal><span lang=PT-BR>12. Então o rei se aproximou do Abençoado e permaneceu em
pé a um lado. Enquanto estava ali em pé - inspecionando a comunidade de
bhikkhus sentada em absoluto silêncio, calma como um lago - ele se sentiu
inspirado para exclamar: “Que o meu filho, Príncipe Udayibhadda, desfrute da
mesma paz que esta comunidade de bhikkhus agora desfruta!&quot; </span></p>

<p class=Normal><span lang=PT-BR>[O Abençoado disse:] “Você veio, grande rei, junto com
aqueles por quem você tem afeto?” </span></p>

<p class=Normal><span lang=PT-BR>“Venerável senhor, meu filho, Príncipe Udayibhadda,[<a
href="#N8">8</a>] <a name=R8></a>é muito querido. Que ele desfrute a mesma paz
que esta comunidade de bhikkhus agora desfruta!” </span></p>

<p class=Normal><span lang=PT-BR>13. Então, curvando-se ante o Abençoado, e saudando a
comunidade de bhikkhus com as suas mãos postas, ele sentou a um lado e disse
para o Abençoado: “Eu gostaria de perguntar ao Abençoado acerca de um certo
assunto, se ele me desse a oportunidade de explicar minha questão.” </span></p>

<p class=Normal><span lang=PT-BR>“Pergunte, grande rei, o que você quiser.” </span></p>

<p class=Normal><b><span lang=PT-BR>(A Pergunta do Rei)</span></b></p>

<p class=Normal><span lang=PT-BR>14. “Venerável senhor, existem estes especialistas:
treinadores de elefantes, treinadores de cavalos, condutores de carruagens,
arqueiros, porta-estandartes, marechais de campo, oficiais do grupo de
abastecimento, oficiais reais de alto escalão, comandos, heróis militares,
guerreiros com armaduras, guerreiros com vestimentas de couro, escravos
domésticos, confeiteiros, barbeiros, banhistas, cozinheiros, artesões de
grinaldas, lavadeiras, tecelões, cesteiros, oleiros, aritméticos, contadores, e
qualquer outro especialista semelhante. Eles vivem dos frutos do seu ofício,
visíveis no aqui e agora. Eles mantêm a si mesmos e aos seus pais, esposa e
filhos, amigos e colegas, com conforto e felicidade. Eles dão excelentes
oferendas, para brâmanes e contemplativos, que os conduzem ao paraíso, contribuem
para a sua felicidade, têm a bem-aventurança como resultado. É possível,
venerável senhor, apontar um fruto similar da vida contemplativa, visível no
aqui e agora?”</span></p>

<p class=Normal><span lang=PT-BR>15. “Você se recorda, grande rei, de alguma vez ter
perguntado isto a outros brâmanes ou contemplativos?”</span></p>

<p class=Normal><span lang=PT-BR>“Sim, eu me lembro.”</span></p>

<p class=Normal><span lang=PT-BR>“Se não for um problema para você, como eles responderam?”</span></p>

<p class=Normal><span lang=PT-BR>“Não, não é um problema responder ao Abençoado, ou
alguém como o Abençoado.” </span></p>

<p class=Normal><span lang=PT-BR>“Então fale, grande rei.” </span></p>


<p class=Normal><a name=T16></a><span lang=PT-BR>16. “Certa vez, venerável senhor eu fui até
Purana Kassapa [<a href="#N9">9</a>] <a name=R9></a>e, chegando, nos
cumprimentamos. Após a troca de saudações amigáveis e corteses, sentei a um
lado e perguntei: ‘Venerável Kassapa, existem estes especialistas ... Eles
vivem dos frutos do seu trabalho, visível no aqui e agora ... É possível,
venerável senhor, apontar um fruto similar da vida contemplativa, visível no
aqui e agora?’ </span></p>

<p class=Normal><span lang=PT-BR>17. “Quando isto foi dito, Purana Kassapa me disse, ‘Grande
rei, agindo ou fazendo com que outros ajam, mutilando ou fazendo com que outros
mutilem, torturando ou fazendo com que outros torturem, causando sofrimento ou
fazendo com que outros causem sofrimento, atormentando ou fazendo com que
outros atormentem, intimidando ou fazendo com que outros intimidem, matando,
tomando o que não é dado, arrombando casas, pilhando riquezas, roubando,
emboscando nas estradas, cometendo adultério, dizendo mentiras - a pessoa não
faz o mal. Se com uma lâmina afiada como uma navalha alguém convertesse todos
os seres vivos sobre a terra em um único amontoado de carne, uma única pilha de
carne, não haveria mal por essa causa, nenhum resultado do mal. Mesmo se alguém
fosse ao longo da margem direita do rio Ganges, matando e fazendo com que
outros matem, mutilando e fazendo com que outros mutilem, torturando e fazendo
com que outros torturem, não haveria mal por essa causa, nenhum resultado do
mal. Mesmo se alguém fosse ao longo da margem esquerda do rio Ganges, dando
oferendas e fazendo com que outros dêem oferendas, por causa disso não haveria mérito e nenhum
resultado do mérito. Através da generosidade, do autocontrole, da contenção e
dizendo a verdade não há mérito por essa causa, nenhum resultado do mérito.’ </span></p>

<p class=Normal><span lang=PT-BR>18. “Dessa forma, quando questionado acerca do fruto da
vida contemplativa, visível no aqui e agora, Purana Kassapa respondeu com a sua
teoria da não ação. Tal como se uma pessoa, perguntada a respeito de manga,
respondesse com uma fruta pão; ou quando perguntada a respeito da fruta pão,
respondesse com uma manga: da mesma forma, quando perguntado a respeito do
fruto da vida contemplativa, visível no aqui e agora, Purana Kassapa respondeu
com a sua teoria da não ação. O pensamento me ocorreu: ‘Como pode uma pessoa
como eu pensar em menosprezar um brâmane ou contemplativo que viva no meu reino?’
[<a href="#N10">10</a>] <a name=R10></a>No entanto eu não concordei e tampouco
critiquei as palavras de Purana Kassapa. Assim sem concordar nem criticar o que
ele disse, e embora estivesse insatisfeito, sem expressar a minha insatisfação,
sem aceitar nem rejeitar o seu ensinamento, eu levantei do meu assento e parti.
</span></p>

<p class=Normal><b><span lang=PT-BR>(Purificação através do samsara)</span></b></p>

<p class=Normal><a name=T19></a><span lang=PT-BR>19. “Em uma outra ocasião eu fui até
Makkhali Gosala [<a href="#N11">11</a>] <a name=R11></a>e, chegando, nos
cumprimentamos. Após a troca de saudações amigáveis e corteses, sentei a um
lado e lhe perguntei: ‘Venerável Gosala, existem estes especialistas ... Eles
vivem dos frutos do seu trabalho, visível no aqui e agora ... É possível,
venerável senhor, apontar um fruto similar da vida contemplativa, visível no
aqui e agora?’ </span></p>

<p class=Normal><span lang=PT-BR>20. “Quando isto foi dito, Makkhali Gosala me disse,
‘Grande rei, não existem causas e condições[<a href="#N12">12</a>] <a
name=R12></a>para a contaminação dos seres. Os seres são contaminados sem
causas e condições. Não há causas e condições para a purificação dos seres.
Os seres são purificados sem causas e condições. A realização de uma dada
condição, de qualquer caráter, não depende quer seja das próprias ações, ou das
ações dos outros, ou do esforço humano. Não há tal coisa como o poder ou
energia, nem o poder humano ou a energia humana. Todos os animais, todas as
criaturas, todos os seres, todas as almas, não têm força, poder e energia por
si mesmos. Eles se inclinam nesta ou naquela direção de acordo com o seu
destino, moldado de acordo com as circunstâncias e natureza da classe à qual
pertencem, de acordo com a sua respectiva natureza: e é de acordo com a sua
posição numa dessas seis classes que eles experimentam o prazer e a dor. </span></p>

<p class=Normal><span lang=PT-BR>“Existem 1.400.000 tipos principais de nascimento, 6.000
outros, e mais 600; existem 500 tipos de kamma,[<a href="#N13">13</a>]<a
name=R13></a> ou 5 tipos [<a href="#N14">14</a>]<a name=R14></a>e 3 tipos,[<a
href="#N15">15</a>] <a name=R15></a>e meio kamma [<a href="#N16">16</a>]; <a
name=R16></a>existem 62 caminhos (modos de conduta), 62 ciclos cósmicos
intermediários, 6 classes (diferenças entre os seres humanos), 8 estágios na
existência humana, 4.900 ocupações, 4.900 tipos de errantes, 4.900 moradas dos
Nagas,[<a href="#N17">17</a>] <a name=R17></a>2.000 existências sencientes,
3.000 infernos, 36 lugares com poeira, 7 classes de renascimento de seres com
consciência, 7 sem consciência, 7 classes de seres ‘livres dos grilhões’, 7
tipos de divindades, 7 tipos de seres humanos, 7 tipos de demônios, 7 lagos, 7
nós, 7 grandes e 7 pequenos precipícios, 7 grandes e 7 pequenos tipos de
sonhos.</span></p>

<p class=Normal><span lang=PT-BR>“Existem 8 milhões e 400 mil grandes ciclos cósmicos
durante os quais, ambos os sábios e os tolos, transmigrando e perambulando
através do ciclo de renascimentos darão um fim ao sofrimento da mesma forma.
Embora os sábios aspirem: ‘Através desta virtude ou observância ou ascetismo ou
desta vida santa eu amadurecerei o kamma que não está maduro e aniquilarei o
kamma amadurecido na medida em que ele surgir.’ [<a href="#N18">18</a>] <a
name=R18></a>Embora os tolos tenham a mesma aspiração, nenhum deles será capaz
de fazer isso. O prazer e a dor são repartidos e não podem ser alterados ao
longo da transmigração, não pode haver o seu incremento nem a sua diminuição,
não há excesso nem falta. Tal como uma bola de linha, quando arremessada, chega
ao seu fim simplesmente desenrolando, da mesma forma, tendo transmigrado e
perambulado através do ciclo de renascimentos, durante o tempo determinado, e
somente depois disso, ambos os sábios e os tolos darão um fim ao sofrimento.” </span></p>

<p class=Normal><span lang=PT-BR>21. “Dessa forma, quando questionado acerca do fruto da
vida contemplativa, visível no aqui e agora, Makkhali Gosala respondeu com a
sua teoria da purificação através da perambulação. Tal como se uma pessoa,
perguntada a respeito de manga, respondesse com uma fruta pão; ou quando
perguntada a respeito da fruta pão, respondesse com uma manga: da mesma forma,
quando perguntado a respeito do fruto da vida contemplativa, visível no aqui e
agora, Makkhali Gosala respondeu com a sua teoria da purificação através da
perambulação. O pensamento me ocorreu: ‘Como pode uma pessoa como eu pensar em
menosprezar um brâmane ou contemplativo que viva no meu reino?’ No entanto eu
não concordei e tampouco critiquei as palavras de Makkhali Gosala. Assim sem
concordar nem criticar o que ele disse, e embora estivesse insatisfeito, sem
expressar a minha insatisfação, sem aceitar nem rejeitar o seu ensinamento, eu
levantei do meu assento e parti. </span></p>

<p class=Normal><b><span lang=PT-BR>(Aniquilação)</span></b></p>

<p class=Normal><a name=T22></a><span lang=PT-BR>22. “Em uma outra ocasião eu fui até Ajita
Kesakambala [<a href="#N19">19</a>] <a name=R19></a>e, chegando, nos cumprimentamos.
Após a troca de saudações amigáveis e corteses, sentei a um lado e lhe
perguntei: ‘Venerável Ajita, existem estes especialistas ... Eles vivem dos
frutos do seu trabalho, visível no aqui e agora ... É possível, venerável
senhor, apontar um fruto similar da vida contemplativa, visível no aqui e
agora?’ </span></p>

<p class=Normal><a name=T23></a><span lang=PT-BR>23. “Quando isso foi dito, Ajita
Kesakambala me disse, ‘Grande rei, não existe nada que é dado, nada que é
oferecido, nada que é sacrificado; não existe fruto ou resultado de ações boas
ou más; não existe este mundo nem outro mundo; não existe mãe, nem pai; nenhum
ser que renasça espontaneamente; não
existem no mundo brâmanes nem contemplativos bons e virtuosos que, após terem
conhecido e compreendido diretamente por eles mesmos, proclamam este mundo e o
próximo. [<a href="#N20">20</a>]<a name=R20></a> Uma pessoa consiste dos quatro grandes elementos. Quando ela morre, a
terra retorna e vai para o corpo da terra, a água retorna e vai para o corpo da
água, o fogo retorna e vai para o corpo do fogo, o ar retorna e vai para o
corpo do ar; as faculdades são transferidas para o espaço. Quatro homens com o
ataúde como quinto levam o corpo embora. As orações funerárias duram até chegar
no cemitério; os ossos branqueiam; as oferendas queimadas terminam como cinzas.
A generosidade é uma doutrina dos tolos. Qualquer um que afirme uma doutrina de
que a generosidade existe, isso é vazio, falsa tagarelice. Tolos e sábios são
da mesma forma extintos e aniquilados com a dissolução do corpo; depois da
morte eles não existem.’ </span></p>

<p class=Normal><span lang=PT-BR>24. “Dessa forma, quando questionado acerca do fruto da
vida contemplativa, visível no aqui e agora, Ajita Kesakambala respondeu com a
