<p class=Normal><b><span lang=PT-BR>(Frutos Mais Elevados da Vida Contemplativa)</span></b></p>

<p class=Normal><span lang=PT-BR>39. “Mas é possível, venerável senhor, apontar mais um
fruto da vida contemplativa, visível no aqui e agora?” </span></p>

<p class=Normal><span lang=PT-BR>“Sim, é possível, grande rei. Ouça e preste muita atenção. </span></p>

<p class=Normal><a name=T40></a><span lang=PT-BR>40. “É o caso, grande rei, que um Tathagata
surge no mundo, um arahant, Perfeitamente Iluminado, consumado no
verdadeiro conhecimento e conduta, bem-aventurado, conhecedor dos mundos, um
líder insuperável de pessoas preparadas para serem treinadas, mestre de devas e
