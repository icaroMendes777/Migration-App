possui atenção plena e plena consciência, e está satisfeito. </span></p>

<p class=Normal><b><span lang=PT-BR>(A Pequena Seção sobre a Virtude)</span></b></p>

<p class=Normal><a name=T43></a><span lang=PT-BR>43-62. “E como um bhikkhu é consumado em
virtude? Abandonando tirar a vida de outros seres, ele se abstém de tirar a
vida de outros seres; ele permanece com a sua vara e arma postas de lado,
bondoso e gentil, compassivo com todos os seres vivos. Isto é parte da sua
virtude. </span></p>

<p class=Normal><span lang=PT-BR>“Abandonando tomar o que não seja dado, ele se abstém de
tomar o que não é dado; tomando somente aquilo que é dado, aceitando somente
aquilo que é dado, não roubando ele permanece puro. Isto, também, é parte da
sua virtude. </span></p>

<p class=Normal><span lang=PT-BR>“Abandonando o não celibato, ele vive uma vida celibatária,
vive separado, abstendo-se da prática vulgar do ato sexual. Isto, também, é
parte da sua virtude. </span></p>

<p class=Normal><a name=linguagem></a><span lang=PT-BR>“Abandonando a linguagem mentirosa,
ele se abstém da linguagem mentirosa. Ele fala a verdade, mantém a verdade, é
firme e confiável, não é um enganador do mundo. Isto, também, é parte da sua
virtude. </span></p>

<p class=Normal><span lang=PT-BR>“Abandonando a linguagem maliciosa, ele se abstém da
linguagem maliciosa. O que ele ouviu aqui ele não conta ali para separar
aquelas pessoas destas. O que ele ouviu lá ele não conta aqui para separar
estas pessoas daquelas. Assim ele reconcilia aquelas pessoas que estão
divididas, promove a amizade, ele ama a concórdia, se delicia com a concórdia,
desfruta da concórdia, diz coisas que criam a concórdia. Isto, também, é parte
da sua virtude. </span></p>

<p class=Normal><span lang=PT-BR>“Abandonando a linguagem grosseira, ele se abstém da
linguagem grosseira. Ele diz palavras que são gentis, que agradam aos ouvidos,
carinhosas, que penetram o coração, que são corteses, desejadas por muitos e
que agradam a muitos. Isto, também, é parte da sua virtude. </span></p>

<p class=Normal><span lang=PT-BR>“Abandonando a linguagem frívola, ele se abstém da
linguagem frívola. Ele fala na hora certa, diz o que é fato, aquilo que é bom,
fala de acordo com o Dhamma e a Disciplina; nas horas adequadas ele diz
palavras que são úteis, racionais, moderadas e que trazem benefício. Isto,
também, é parte da sua virtude. </span></p>

<p class=Normal><span lang=PT-BR>“Ele se abstém de danificar sementes e plantas. </span></p>

<p class=Normal><span lang=PT-BR>“Ele come somente uma vez ao dia, privando-se da refeição
noturna e de alimentos nas horas incorretas. </span></p>

<p class=Normal><a name=espetaculos></a><span lang=PT-BR>“Ele se abstém de dançar, cantar,
ouvir música e de ver espetáculos de entretenimento. </span></p>

<p class=Normal><a name=perfumes></a><span lang=PT-BR>“Ele se abstém de usar ornamentos,
usar perfumes e de embelezar o corpo com cosméticos. </span></p>

<p class=Normal><a name=luxo></a><span lang=PT-BR>“Ele se abstém de deitar em leitos
elevados e luxuosos. </span></p>

<p class=Normal><a name=ouro></a><span lang=PT-BR>“Ele se abstém de aceitar ouro e dinheiro.
</span></p>

<p class=Normal><span lang=PT-BR>“Ele se abstém de aceitar grãos que não estejam cozidos ...
carne crua ... mulheres e garotas ... escravos e escravas ... cabras e ovelhas
... aves e porcos ... elefantes, gado, cavalos e éguas ... terras e
propriedades. </span></p>

<p class=Normal><span lang=PT-BR>“Ele se abstém de fazer pequenas tarefas e levar mensagens
... de comprar e vender ... de lidar com balanças falsas, metais falsos, falsas
medidas ... do suborno, burla e fraude. </span></p>

<p class=Normal><span lang=PT-BR>“Ele se abstém de mutilar, executar, aprisionar, roubar,
pilhar e violentar.</span></p>

<p class=Normal><span lang=PT-BR>“Isto, também, é parte da sua virtude.</span></p>

<p class=Normal><b><span lang=PT-BR>(A Seção Intermediária sobre a Virtude)</span></b></p>

