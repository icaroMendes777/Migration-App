<p class=Normal><span lang=PT-BR><b>Introdução de Thanissaro Bhikkhu</b></span></p>

<p class=Normal>Este discurso é uma das obras primas do Cânone em Pali.
O que é apresentado é um retrato abrangente do método de treinamento Budista,
com a ilustração de cada estágio do treinamento com vívidos símiles. Esse
retrato é colocado em justaposição com a visão Budista dos ensinamentos de
mestres filosóficos rivais da época, mostrando como o Buda - contrariamente à
abordagem inflexível e sectária dos seus contemporâneos - apresenta os seus
ensinamentos de uma forma que é pertinente e sensível às necessidades dos seus
ouvintes. Esse retrato mais amplo do contexto intelectual da Índia nesse
período inicial do Budismo é então apresentado como uma narrativa: a triste
história do rei Ajatasattu. </span></p>

<p class=Normal><span lang=PT-BR>Ajatasattu era o filho do rei Bimbisara de Magadha, um dos
primeiros discípulos do Buda. Encorajado por Devadatta - o primo do Buda, que
queria usar o apoio de Ajatasattu na sua tentativa de tomar a posição do Buda
como chefe da Sangha - Ajatasattu organizou a morte do próprio pai de forma que
ele pudesse assegurar sua própria subida ao trono. Como resultado dessa má
ação, ele estava destinado a não somente ser morto por seu próprio filho -
Udayibhadda (mencionado no discurso) - mas também de imediatamente renascer em
uma das regiões inferiores do inferno. </span></p>

<p class=Normal><span lang=PT-BR>Neste discurso, Ajatasattu visita o Buda na esperança de
que este possa trazer um pouco de paz para a sua mente. A questão que ele
coloca para o Buda mostra o limitado nível do seu próprio entendimento, dessa
forma o Buda pacientemente descreve os passos do treinamento, começando pelo
nível mais básico e gradualmente evoluindo, como uma forma de ampliar o
horizonte espiritual do rei. Ao final do discurso, Ajatasattu toma refúgio na
Jóia Tríplice. Embora as ações praticadas anteriormente eram tão pesadas que
essa expressão de fé poderia ter uma conseqüência apenas limitada no presente
imediato, o comentário assegura que a história do rei acabaria tendo um final
feliz. Após a morte do Buda, ele patrocinou o Primeiro Concílio, no qual um
congresso de arahants produziu o primeiro conjunto padronizado dos ensinamentos
do Buda. Como resultado do mérito proveniente desse ato, Ajatasattu estava
destinado - após a sua libertação do inferno - a alcançar a Iluminação como um
Paccekabuddha.</span></p>

