“Um bhikkhu afastado dos prazeres sensuais, afastado das qualidades não hábeis, entra e permanece no primeiro jhana, que é caracterizado pelo pensamento aplicado e sustentado, com o êxtase e felicidade nascidos do afastamento. Ele permeia e impregna, cobre e preenche o corpo com o êxtase e felicidade nascidos do afastamento. Não há nada em todo o corpo que não esteja permeado pelo êxtase e felicidade nascidos do afastamento.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR>76. "É como se um banhista habilidoso ou seu aprendiz vertesse pó de banho numa bacia de latão e o misturasse, borrifando com água de tempos em tempos, de forma que essa bola de pó de banho - saturada, carregada de umidade, permeada por dentro e por fora - no entanto não pingasse; assim, o bhikkhu permeia, cobre e preenche o corpo com o êxtase e felicidade nascidos do afastamento...</span></p>

<p class=Normal><span lang=PT-BR>“Este é um fruto da vida contemplativa, visível no aqui e
agora, mais excelente do que os anteriores e mais sublime. </span></p>

<p class=Normal><a name=T77></a><span lang=PT-BR>77. "E além disso, abandonando o pensamento aplicado e sustentado, um bhikkhu entra e permanece no segundo jhana, que é caracterizado pela segurança interna e perfeita unicidade da mente, sem o pensamento aplicado e sustentado, com o êxtase e felicidade nascidos da concentração. Ele permeia e impregna, cobre e preenche o corpo com o êxtase e felicidade nascidos da concentração. Não há nada em todo o corpo que não esteja permeado pelo êxtase e felicidade nascidos da concentração. </span></p>

<p class=Normal><span lang=PT-BR>78. "Como um lago sendo alimentado por uma fonte de água interna, não tendo um fluxo de água do leste, oeste, norte, ou sul, nem os céus periodicamente fornecendo chuvas abundantes, de modo que a fonte de água interna permeia e impregna, cobre e preenche o lago de água fresca, sem que nenhuma parte do lago não esteja permeada pela água fresca; assim também o bhikkhu permeia e impregna, cobre e preenche o corpo com o êxtase e felicidade nascidos da concentração...</span></p>


<p class=Normal><span lang=PT-BR>“Este, também, é um fruto da vida contemplativa, visível no
aqui e agora, mais excelente do que os anteriores e mais sublime. </span></p>

<p class=Normal><span lang=PT-BR>79. "E além disso, abandonando o êxtase, um bhikkhu entra e permanece no terceiro jhana que é caracterizado pela felicidade sem o êxtase, acompanhada pela atenção plena, plena consciência e equanimidade, acerca do qual os nobres declaram: ‘Ele permanece numa estada feliz, equânime e plenamente atento.’ Ele permeia e impregna, cobre e preenche o corpo com a felicidade despojada do êxtase, de forma que não exista nada em todo o corpo que não esteja permeado com a felicidade despojada do êxtase.</span></p>

<p class=Normal><span lang=PT-BR>80. "Como num lago que tenha flores de lótus azuis, brancas ou vermelhas, podem existir algumas flores de lótus azuis, brancas, ou vermelhas que, nascidas e tendo crescido na água, permanecem imersas na água e florescem sem sair de dentro da água, de forma que elas permanecem permeadas e impregnadas, cobertas e preenchidas com água fresca da raiz até a ponta, e nada dessas flores de lótus azuis, brancas ou vermelhas permanece sem estar permeado pela água fresca; assim também o bhikkhu permeia e impregna, cobre e preenche o corpo com a felicidade despojada de êxtase...</span></p>

<p class=Normal><span lang=PT-BR>“Este, também, é um fruto da vida contemplativa, visível no
aqui e agora, mais excelente do que os anteriores e mais sublime. </span></p>

<p class=Normal><span lang=PT-BR>81. "E além disso, com o completo desaparecimento da felicidade, um bhikkhu entra e permanece no quarto jhana, que possui nem felicidade nem sofrimento, com a atenção plena e a equanimidade purificadas. Ele permanece permeando o corpo com a mente pura e luminosa, de forma que não exista nada em todo o corpo que não esteja permeado pela mente pura e luminosa. </span></p>

<p class=Normal><span lang=PT-BR>82. "Como se um homem estivesse enrolado da cabeça aos pés com um tecido branco de forma que não houvesse nenhuma parte do corpo que não estivesse coberta pelo tecido branco; assim também o bhikkhu permanece permeando o corpo com a mente pura e luminosa. Não há nada no corpo que não esteja permeado por essa mente pura e luminosa.</span></p>

<p class=Normal><span lang=PT-BR>“Este, também, é um fruto da vida contemplativa, visível no
