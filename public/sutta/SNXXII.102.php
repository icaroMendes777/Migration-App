<p class=Normal> Em Savatthi. Quando a percepção da impermanência é desenvolvida e cultivada esta elimina toda cobiça sensual, elimina toda cobiça por ser/existir, elimina toda ignorância, elimina toda presunção 'eu sou'. </p>

<p class=Normal> "Tal como, bhikkhus, no outono um agricultor operando um grande arado corta todas as raízes, assim também, quando a percepção da impermanência é desenvolvida e cultivada esta elimina toda cobiça sensual, elimina toda cobiça por ser/existir, elimina toda ignorância, elimina toda presunção 'eu sou'. </p>

<p class=Normal> "Tal como um cortador de junco depois de cortar o caule o agarra pelo topo e o agita e bate, assim também, quando a percepção da impermanência é desenvolvida e cultivada esta elimina toda cobiça sensual, elimina toda cobiça por ser/existir, elimina toda ignorância, elimina toda presunção 'eu sou'. </p>

<p class=Normal> "Tal como quando um galho de uma mangueira é cortado todas as mangas presas ao galho seguem com o galho, assim também, quando a percepção da impermanência é desenvolvida e cultivada esta elimina toda cobiça sensual, elimina toda cobiça por ser/existir, elimina toda ignorância, elimina toda presunção 'eu sou'. </p>

<p class=Normal> “Tal como os caibros de uma casa com cumeeira se dirigem todos para a cumeeira, se inclinam para a cumeeira, convergem na cumeeira e a cumeeira é considerada como a melhor entre todos eles, assim também, quando a percepção da impermanência é desenvolvida e cultivada esta elimina toda cobiça sensual, elimina toda cobiça por ser/existir, elimina toda ignorância, elimina toda presunção 'eu sou'. </p>

<p class=Normal> “Tal como entre todas as raízes perfumadas, a raiz da íris negra é considerada como contendo o melhor perfume entre as raízes, assim também, quando a percepção da impermanência é desenvolvida e cultivada esta elimina toda cobiça sensual, elimina toda cobiça por ser/existir, elimina toda ignorância, elimina toda presunção 'eu sou'. </p>

<p class=Normal> “Tal como entre todas as madeiras perfumadas, o sândalo vermelho é considerado como o melhor perfume entre as madeiras, assim também, quando a percepção da impermanência é desenvolvida e cultivada esta elimina toda cobiça sensual, elimina toda cobiça por ser/existir, elimina toda ignorância, elimina toda presunção 'eu sou'. </p>

<p class=Normal> “Tal como entre todas as flores perfumadas, o jasmim é considerado o melhor perfume entre as flores, assim também, quando a percepção da impermanência é desenvolvida e cultivada esta elimina toda cobiça sensual, elimina toda cobiça por ser/existir, elimina toda ignorância, elimina toda presunção 'eu sou'. </p>

<p class=Normal> “Tal como todos os príncipes menores se tornam vassalos de um monarca que gira a roda e o monarca que gira a roda é considerado como o melhor entre todos eles, assim também, quando a percepção da impermanência é desenvolvida e cultivada esta elimina toda cobiça sensual, elimina toda cobiça por ser/existir, elimina toda ignorância, elimina toda presunção 'eu sou'. </p>

<p class=Normal> “Tal como a luminosidade das constelações não equivale a um dezesseis avos da luminosidade da lua, e a luminosidade da lua é considerada como a melhor entre todas elas, assim também, quando a percepção da impermanência é desenvolvida e cultivada esta elimina toda cobiça sensual, elimina toda cobiça por ser/existir, elimina toda ignorância, elimina toda presunção 'eu sou'. </p>

<p class=Normal> “Tal como no outono, no último mês da estação das chuvas, quando o céu está limpo e sem nuvens, o sol surge na terra dissipando toda a escuridão do espaço com a sua luminosidade brilhante e radiante, quando a percepção da impermanência é desenvolvida e cultivada esta elimina toda cobiça sensual, elimina toda cobiça por ser/existir, elimina toda ignorância, elimina toda presunção 'eu sou'. </p>

<p class=Normal> "E como, bhikkhus, a percepção da impermanência é desenvolvida e cultivada de modo que elimine toda cobiça sensual, elimine toda cobiça por ser/existir, elimine toda ignorância, elimine toda presunção 'eu sou'? 'Assim é a forma, essa é a sua origem, essa é a sua cessação. Assim
é a sensação, essa é a sua origem, essa é a sua cessação. Assim é a percepção,
essa é a sua origem, essa é a sua cessação. Assim são as formações volitivas, essa é a
sua origem, essa é a sua cessação. Assim é a consciência, essa é a sua origem,
essa é a sua cessação': assim é como a percepção da impermanência é desenvolvida e cultivada de modo que elimine toda cobiça sensual, elimine toda cobiça por ser/existir, elimine toda ignorância, elimine toda presunção 'eu sou'". </p>


