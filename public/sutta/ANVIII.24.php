<p class=Normal> Em certa ocasião, o Abençoado estava em Alavi no santuário Aggalava. Então Hatthaka de Alavi acompanhado por quinhentos discípulos leigos foi até o Abençoado e depois de cumprimentá-lo sentou a um lado. O Abençoado então disse:</p>

<p class=Normal>"A sua comitiva é grande, Hatthaka. Como você mantém essa comitiva tão grande?"</p>

<p class=Normal><a name=R1></a>"Eu assim faço, venerável senhor, por meio dos quatro modos para manter um relacionamento favorável ensinados pelo Abençoado. Quando eu sei: 'Este deve ser tratado com uma oferenda,' eu o trato com uma oferenda. Quando eu sei: 'Este deve ser tratado com um discurso terno,' eu o trato com um discurso terno. Quando eu sei: 'Este deve ser tratado com a boa conduta,' eu o trato com a boa conduta. Quando eu sei: 'Este deve ser tratado com a imparcialidade,' eu o trato com a imparcialidade. A minha família possui riqueza, venerável senhor. Eles não pensam que devem me ouvir como se eu fosse pobre." [<a href="#N1">1</a>]</p>

<p class=Normal>"Muito bem, Hatthaka! Esse é o método através do qual você é capaz de manter uma grande comitiva. Pois todos aqueles que no passado mantiveram uma grande comitiva assim fizeram através desses mesmos quatro modos para manter um relacionamento favorável. Todos aqueles que no futuro manterão uma grande comitiva assim farão através desses mesmos quatro modos para manter um relacionamento favorável. Todos aqueles que no presente mantêm uma grande comitiva assim fazem através desses mesmos quatro modos para manter um relacionamento favorável."</p>

<p class=Normal>Então depois do Abençoado ter instruído, motivado, estimulado e encorajado Hatthaka de Alavi com um discurso do Dhamma, Hatthaka levantou do seu assento e depois de homenagear o Abençoado, mantendo-o à sua direita, partiu.</p>

<p class=Normal>Então, não muito tempo depois que Hatthaka de Alavi tinha partido, o Abençoado se dirigiu aos bhikkhus: "Bhikkhus, vocês devem se recordar de Hatthaka de Alavi como aquele que possui oito qualidades maravilhosas e admiráveis. Quais oito? Ele está dotado de fé. (2) Ele é virtuoso, e (3) possui a vergonha e (4)  o temor de cometer transgressões. (5) Ele é estudado, (6) generoso, e (7) sábio. (8) Ele tem poucos desejos. Vocês devem se recordar de Hatthaka de Alavi como aquele que possui oito qualidades maravilhosas e admiráveis."
 </p>

