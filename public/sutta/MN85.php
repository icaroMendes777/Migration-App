<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[2] MA explica que o Príncipe Bodhi não tinha filhos e desejava ter um.
Ele havia ouvido que as pessoas podem ter os seus desejos satisfeitos fazendo
oferendas especiais para o Buda, portanto ele cobriu tudo com o tecido branco
