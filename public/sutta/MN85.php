<p class=Normal style='margin-left:1.0in'>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. </span>Assim
ouvi. Em certa ocasião o Abençoado estava entre os<span lang=PT-BR
style='mso-ansi-language:PT-BR'> Bhaggas em Surilsumaragira no Bosque de
Bhesakala, no Parque do Gamo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. Agora,
aquela ocasião um palácio com o nome de Kokanada havia sido construído
recentemente para o Príncipe Bodhi e ainda não havia sido habitado de modo
nenhum por nenhum contemplativo ou brâmane ou ser humano. [<a href="#N1">1</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>3. </span>Então, <span lang=PT-BR style='mso-ansi-language:PT-BR'>o
Príncipe Bodhi</span> se dirigiu ao estudante brâmane <span lang=PT-BR
style='mso-ansi-language:PT-BR'>Sanjikaputta</span>: “Venha, estimado <span
lang=PT-BR style='mso-ansi-language:PT-BR'>Sanjikaputta</span>, vá até o
Abençoado e preste uma homenagem em meu nome com a sua cabeça aos pés dele e
pergunte se ele está livre de enfermidades e aflições, se está com saúde, forte
e vivendo com conforto, dizendo:<span style="mso-spacerun: yes"> 
</span>‘Venerável senhor, <span lang=PT-BR style='mso-ansi-language:PT-BR'>o
Príncipe Bodhi</span> homenageia o Abençoado com a cabeça aos seus pés e
pergunta se o Abençoado está livre de enfermidades ... e vivendo com conforto.’
Então diga o seguinte: ‘Venerável senhor, que o Abençoado junto com a Sangha de
Bhikkhus concorde em aceitar a refeição de amanhã do Príncipe Bodhi.’”<span
lang=PT-BR style='mso-ansi-language:PT-BR'><o:p></o:p></span></p>



<p class=Normal>“Sim, senhor,” <span lang=PT-BR style='mso-ansi-language:
PT-BR'>Sanjikaputta</span> respondeu e foi até o Abençoado e ambos se cumprimentaram.
Quando a conversa cortês e amigável havia terminado, ele se sentou a um lado e
disse: ‘Mestre Gotama, <span lang=PT-BR style='mso-ansi-language:PT-BR'>o
Príncipe Bodhi</span> presta uma homenagem com a cabeça aos pés do Mestre
Gotama e pergunta se ele está livre de enfermidades ... e vivendo com conforto. E
ele diz o seguinte<span lang=PT-BR style='mso-ansi-language:PT-BR'>: ‘Q</span>ue
o Mestre Gotama junto com a Sangha dos Bhikkhus concorde em aceitar a refeição
de amanhã do Príncipe Bodhi.’”<span lang=PT-BR style='mso-ansi-language:PT-BR'><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. O
Abençoado consentiu em silêncio. Então, quando ele viu que ele havia
concordado, Sanjikaputta se levantou do seu assento, foi até o Príncipe Bodhi e
relatou o que havia ocorrido, acrescentando: “O contemplativo Gotama
concordou.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. Então,
quando a noite terminou, o Príncipe Bodhi fez com que fossem preparados vários
tipos de boa comida na sua própria residência, e fez com que o Palácio Kokanada
fosse coberto com tecido branco até o último degrau da escadaria. Então, ele se
dirigiu ao </span>estudante brâmane <span lang=PT-BR style='mso-ansi-language:
PT-BR'>Sanjikaputta: “Venha, estimado Sanjikaputta, vá até o Abençoado e
anuncie que chegou a hora, assim:<span style="mso-spacerun: yes">  </span>‘É hora,
venerável senhor, a refeição está pronta.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
senhor,” Sanjikaputta respondeu e foi até o Abençoado e anunciou que havia
chegado a hora, assim:<span style="mso-spacerun: yes">  </span>‘É hora, </span>Mestre
Gotama<span lang=PT-BR style='mso-ansi-language:PT-BR'>, a refeição está
pronta.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. Então,
ao amanhecer, o Abençoado se vestiu e tomando a sua tigela e o manto externo,
ele foi até a residência do Príncipe Bodhi.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. Agora,
naquela ocasião o Príncipe Bodhi estava em pé na varanda externa esperando pelo
Abençoado. Ao ver o Abençoado vindo à distância, ele saiu para recebê-lo e
homenageá-lo; e depois, permitindo que o Abençoado fosse à sua frente, eles
seguiram para o Palácio Kokanada. Mas o Abençoado parou no primeiro degrau da
escadaria. O Príncipe Bodhi disse: “Venerável senhor, que o Abençoado pise
sobre o tecido, que o Sublime pise sobre o tecido, isso irá conduzir ao meu bem-estar e felicidade por muito tempo.” Quando isso foi dito o Abençoado
permaneceu em silêncio. [<a href="#N2">2</a>] <o:p></o:p></span></p>



<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Uma segunda vez ... uma terceira vez o Príncipe Bodhi disse:<span
style="mso-spacerun: yes">  </span>“Venerável senhor, que o Abençoado pise
sobre o tecido, que o Sublime pise sobre o tecido, isso irá conduzir ao meu bem-estar e felicidade por muito tempo.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O Abençoado
olhou para o venerável Ananda. O venerável Ananda disse para o Príncipe Bodhi:
“Príncipe, faça com que o tecido seja removido. O Abençoado não irá pisar numa
faixa de tecido; o Tathagata tem compaixão pelas gerações futuras.” [<a
href="#N3">3</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>8. Assim o Príncipe Boddhi fez com que o tecido fosse removido e
providenciou para que fossem preparados assentos nos aposentos superiores do
Palácio Kokanada. O Abençoado e a Sangha dos Bhikkhus subiram até os aposentos
superiores do Palácio Kokanada e sentaram nos assentos que haviam sido
preparados.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. Então,
com as suas próprias mãos, o Príncipe Boddhi serviu e satisfez a Sangha dos
bhikkhus liderada pelo Buda com os vários tipos de alimentos. Quando o
Abençoado havia terminado de comer e removeu a mão da sua tigela, o Príncipe
Boddhi tomou um assento mais baixo e disse para o Abençoado: “Venerável senhor,
nós pensamos assim: ‘O prazer não é obtido através do prazer; o prazer deve ser
obtido através da dor.’” [<a href="#N4">4</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>10. “Príncipe, antes da minha iluminação, quando eu ainda era apenas um
Bodisatva não iluminado, eu também pensava assim: ‘O prazer não é obtido
através do prazer; o prazer deve ser obtido através da dor.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11-14.
“Mais tarde, Príncipe, ainda jovem, um homem jovem com o cabelo negro, dotado
com as bênçãos da juventude, na flor da juventude ...<span style="mso-spacerun:
yes">  </span>(igual ao <a href="MN26.php#T14">MN 26, versos 14-17</a>) ... E
eu me sentei ali pensando: Isso é adequado para o esforço.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15-42.
“Agora estes três símiles, nunca ouvidos antes, me ocorreram espontaneamente
... (igual ao <a href="MN36.php#T17">MN 36, versos 17-44</a>, mas neste sutta
nos versos 18-23 que correspondem aos versos 20-25 do MN 36, a sentença “Porém,
a sensação de dor que surgiu em mim não invadiu a minha mente e permaneceu”,
não está presente, e neste sutta nos versos 37, 39 and 42 que correspondem aos
versos 39, 41 e 44 do MN 36, a sentença “Mas o sentimento prazeroso que surgiu
em mim não invadiu a minha mente e permaneceu”, não está presente) ...<span
style="mso-spacerun: yes">  </span>como acontece com alguém que seja diligente,
ardente e decidido.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>43-53. “Eu
pensei: ‘Este Dhamma que eu alcancei é profundo ... (igual ao <a
href="MN26.php#T19">MN 26, versos 19-29</a>) ... e todos nós seis vivíamos
daquilo que aqueles dois bhikkhus traziam de esmolas.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>54. “Então,
o grupo de cinco bhikkhus não muito tempo depois de terem sido ensinados e
instruídos por mim, </span>realizando por si mesmos com o conhecimento direto,
aqui e agora entraram e permaneceram no que é o objetivo supremo da vida santa
pelo qual com razão membros de um clã adotam a vida santa<span lang=PT-BR
style='mso-ansi-language:PT-BR'>.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>55. Quando
isso foi dito, o Príncipe Bodhi disse para o Abençoado: “Venerável senhor,
quando um bhikkhu encontra o Tathagata para treiná-lo, quanto tempo tarda até
que </span>realizando por si mesmo com o conhecimento direto, aqui e agora ele
entre e permaneça no que é o objetivo supremo da vida santa pelo qual com razão
membros de um clã adotam a vida santa<span lang=PT-BR style='mso-ansi-language:
PT-BR'>?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quanto a
isso, príncipe, eu lhe farei uma pergunta em retorno. Responda como quiser. O
que você pensa, príncipe? Você é hábil na arte de empregar uma aguilhada com
destreza ao montar um elefante?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
venerável senhor, eu sou,”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>56. “O que
você pensa, príncipe? Suponha que um homem aqui viesse pensando: ‘O Príncipe
Bodhi conhece a arte de empregar uma aguilhada ao montar um elefante; eu devo
treinar nessa arte com ele.’ Se ele não tivesse fé, ele não poderia alcançar
aquilo que pode ser alcançado por aquele que tem fé; se ele fosse muito
enfermo, ele não poderia alcançar aquilo que pode ser alcançado por aquele que
está livre de enfermidades; se ele fosse fraudulento e enganador, ele não
poderia alcançar aquilo que pode ser alcançado por aquele que é honesto e
sincero; se ele fosse preguiçoso, ele não poderia alcançar aquilo que pode ser
alcançado por aquele que é energético; se ele não fosse sábio, ele não poderia
alcançar aquilo que pode ser alcançado por aquele que é sábio. O que você
pensa, príncipe? Esse homem poderia ser treinado por você na arte de empregar
uma aguilhada ao montar um elefante?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, mesmo se ele tivesse uma dessas deficiências, ele não poderia treinar
comigo, então o que dizer de cinco?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>57. “O que
você pensa, príncipe? Suponha que um homem aqui viesse pensando: ‘O Príncipe
Bodhi conhece a arte de empregar uma aguilhada ao montar um elefante; eu devo
treinar nessa arte com ele.’ Se ele tivesse fé, ele poderia alcançar aquilo que
pode ser alcançado por aquele que tem fé; se ele não fosse muito enfermo, ele
poderia alcançar aquilo que pode ser alcançado por aquele que está livre de
enfermidades; se ele não fosse fraudulento e enganador, ele poderia alcançar
aquilo que pode ser alcançado por aquele que é honesto e sincero; se ele não
fosse preguiçoso, ele poderia alcançar aquilo que pode ser alcançado por aquele
que é energético; se ele fosse sábio, ele poderia alcançar aquilo que pode ser
alcançado por aquele que é sábio. O que você pensa, príncipe? Esse homem
poderia ser treinado por você na arte de empregar uma aguilhada ao montar um
elefante?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, se ele tivesse uma dessas qualidades ele poderia ser treinado por mim,
então o que dizer de cinco?”<o:p></o:p></span></p>



<p class=Normal><a name=T58></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>58. “Da mesma forma, príncipe, existem cinco fatores para o esforço.
Quais cinco? Aqui um bhikkhu tem fé, ele deposita fé na iluminação do Tathagata
assim: “Esse </span>Abençoado é um arahant, perfeitamente iluminado,
consumado no verdadeiro conhecimento e conduta, bem-aventurado, conhecedor dos
mundos, um líder insuperável de pessoas preparadas para serem treinadas, mestre
de devas e humanos, desperto, sublime.”<span lang=PT-BR style='mso-ansi-language:
PT-BR'><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então, ele se
vê livre de enfermidades e aflições, possuindo uma boa digestão que não é nem
demasiado fria nem demasiado quente, mas média e capaz de suportar a tensão do
esforço.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então, ele
é honesto e sincero, e se mostra como ele na verdade é para o Mestre e os seus
companheiros na vida santa.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então, ele
é energético no abandono dos estados prejudiciais e no empenho pelos estados
benéficos, decidido, dedicando-se ao esforço com firmeza e perseverando no
cultivo de estados benéficos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então, ele
é sábio; ele possui a completa compreensão da origem e cessação que é nobre e
penetrante, conduzindo à completa destruição do sofrimento. Esses são os cinco
fatores para o esforço.<o:p></o:p></span></p>



<p class=Normal><a name=T59></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>59. “Príncipe, quando um bhikkhu que possui esses cinco fatores para o
esforço encontra um Tathagata para treiná-lo, ele poderá permanecer por sete
anos até que </span>realizando por si mesmo com o conhecimento direto, aqui e
agora ele entre e permaneça no que é o objetivo supremo da vida santa pelo qual
com razão membros de um clã adotam a vida santa<span lang=PT-BR
style='mso-ansi-language:PT-BR'>. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sem falar
em sete anos, príncipe. Quando um bhikkhu que possui esses cinco fatores para o
esforço encontra um Tathagata para treiná-lo, ele poderá permanecer por seis
anos ... cinco anos ... quatro anos ... três anos ... dois anos ... um ano ...
Sem falar em um ano, príncipe, ele poderá permanecer por sete meses ... seis
meses ... cinco meses ... quatro meses ... três meses ... dois meses ... um mês
... meio mês ... Sem falar em meio mês, príncipe, ele poderá permanecer por
sete dias e noites ... seis dias e noites ... cinco dias e noites ... quatro
dias e noites ... três dias e noites ... dois dias e noites ... um dia e noite.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sem falar
em um dia e noite, príncipe. Quando um bhikkhu que possui esses cinco fatores
para o esforço encontra um Tathagata para treiná-lo, então sendo instruído à
noite, ele poderá alcançar a distinção pela manhã, sendo instruído pela manhã
ele poderá alcançar a distinção à noite. “<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>60. Quando
isso foi dito, o Príncipe Bodhi disse para o Abençoado: “Oh o Buda! Oh o
Dhamma! Oh, quão bem proclamado é o Dhamma! Pois alguém que seja instruído à
noite poderá alcançar a distinção pela manhã e alguém instruído pela manhã
poderá alcançar a distinção à noite.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>61. Quando
isso foi dito o </span>estudante brâmane <span lang=PT-BR style='mso-ansi-language:
PT-BR'>Sanjikaputta disse para o Príncipe Bodhi: “Mestre Bodhi diz: ‘Oh o Buda!
Oh o Dhamma! Oh, quão bem proclamado é o Dhamma!’ Mas ele não diz: ‘Eu busco
refúgio no Mestre Gotama e no Dhamma e na Sangha dos bhikkhus.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não diga
isso, estimado Sanjikaputta, não diga isso. Eu ouvi e aprendi da boca da minha
mãe: Em certa ocasião o Abençoado estava em Kosambi no Parque de Ghosita, então
a minha mãe, estando grávida, foi até o Abençoado e depois de cumprimentá-lo,
sentou-se a um lado e disse: ‘Venerável senhor, o príncipe ou princesa no meu
ventre, qualquer um que seja, busca refúgio no Abençoado e no Dhamma e na
Sangha dos bhikkhus. </span>Que o Abençoado se recorde [do feto] como um
discípulo leigo que nele buscou refúgio para o resto da sua vida.’ Também houve
uma ocasião na qual o Abençoado estava entre os<span lang=PT-BR
style='mso-ansi-language:PT-BR'> Bhaggas em Surilsumaragira no Bosque de
Bhesakala, no Parque do Gamo. Então a minha ama, carregando-me sobre o quadril,
foi até o Abençoado e depois de cumprimentá-lo, ficou em pé a um lado e disse:
‘Venerável senhor, o Príncipe Bodhi busca refúgio no Abençoado e no Dhamma e na
Sangha dos bhikkhus. </span>Que o Abençoado se recorde dele como um discípulo
leigo que nele buscou refúgio para o resto da sua vida.’ Agora, estimado<span
lang=PT-BR style='mso-ansi-language:PT-BR'> Sanjikaputta, pela terceira vez eu
busco refúgio no Abençoado e no Dhamma e na Sangha dos bhikkhus. </span>Que o
Abençoado se recorde de mim como um discípulo leigo que nele buscou refúgio
para o resto da sua vida.” <span lang=PT-BR style='mso-ansi-language:PT-BR'><o:p></o:p></span></p>


