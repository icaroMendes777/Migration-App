<p class=Normal><a name=N3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[3] MA diz que essas divindades, que vieram de vários mundos celestiais,
haviam sido companheiras de Rahula durante uma vida passada na qual ele aspirou
alcançar o estado de arahant como filho de um Buda. [</span><a href="#R3"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>Retorna</span></a><span lang=PT-BR
