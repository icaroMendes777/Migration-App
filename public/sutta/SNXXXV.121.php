<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[2] <i>Vimuttiparipacaniya dhamma</i>. MA menciona quinze estados que
amadurecem na libertação: as cinco faculdades espirituais; os cinco insights
penetrativos – da impermanência, sofrimento, não-eu, abandono e cessação; e as
cinco coisas ensinadas para Meghiya – nobre amizade, virtude, discussão
benéfica, energia e sabedoria (veja o <a href="ANIX.3.php">AN IX.3</a>; <a href="UdIV1.php">Ud 4.1</a>). [</span><a
href="#R2"><span lang=PT-BR style='mso-ansi-language:PT-BR'>Retorna</span></a><span
