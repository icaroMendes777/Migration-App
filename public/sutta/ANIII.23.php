<p class=Normal style='margin-left:.75in'> 

<p class=Normal> "Bhikkhus, há três tipos de pessoas que podem ser encontradas no mundo. Quais três? </p>

<p class=Normal>"Aqui, bhikkhus, alguém gera uma formação corporal aflitiva, uma formação verbal aflitiva, uma formação mental aflitiva. Tendo gerado uma formação corporal aflitiva, uma formação verbal aflitiva, uma formação mental aflitiva, ele renasce em um mundo com aflição. Quando ele renasce em um mundo com aflição, contatos aflitivos o tocam. Sendo tocado por contatos aflitivos, ele sente sensações aflitivas, extremamente dolorosas como no caso de seres no inferno. </p>

<p class=Normal>"Alguém gera uma formação corporal sem aflição, uma formação verbal sem aflição, uma formação mental sem aflição. Tendo gerado uma formação corporal sem aflição, uma formação verbal sem aflição, uma formação mental sem aflição, ele renasce num mundo sem aflição. Quando ele renasce num mundo sem aflição, contatos sem aflição o tocam. Sendo tocado por contatos sem aflição, ele sente sensações sem aflição, extremamente prazerosas como no caso dos devas Subhakinna. </p>

<p class=Normal>"Alguém gera uma formação corporal que é tanto aflitiva como sem aflição, uma formação verbal que é tanto aflitiva como sem aflição, uma formação mental que é tanto aflitiva como sem aflição. Tendo gerado uma formação corporal que é tanto aflitiva como sem aflição, uma formação verbal que é tanto aflitiva como sem aflição, uma formação mental que é tanto aflitiva como sem aflição, ele renasce num mundo que é tanto aflitivo como sem aflição. Quando ele renasce num mundo que é tanto aflitivo como sem aflição, contatos que são tanto aflitivos como sem aflição o tocam. Sendo tocado por contatos que são tanto aflitivos como sem aflição, ele sente sensações que são tanto aflitivas como sem aflição, prazer e dor misturados, como no caso dos seres humanos e alguns devas e alguns seres nos mundos inferiores. </p>

<p class=Normal>"Esses, bhikkhus, são os três tipos de pessoas que podem ser encontradas no mundo." </p>

