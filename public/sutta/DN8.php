<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião, o Abençoado estava<span style='mso-spacerun:yes'> 
</span>em Ujunnaya no parque dos gamos Kannakatthale.[<a href="#N1">1</a>] <a
name=R1></a>Então, o contemplativo nu Kassapa foi até o Abençoado e ambos se
cumprimentaram. Quando a conversa cortês e amigável havia terminado ele ficou
em pé a um lado e disse:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. “Amigo
Gotama, eu ouvi dizerem: ‘O contemplativo Gotama desaprova todas as austeridades
e critica e censura todos aqueles que vivem dedicados à mortificação. [<a
href="#N2">2</a>] <a name=R2></a>Venerável senhor, aqueles que assim dizem,
falam aquilo que foi dito pelo contemplativo Gotama e não o deturpam com algo
contrário aos fatos? Eles explicam de acordo com o Dhamma de tal modo que nada
que possa dar margem à censura possa de forma legítima ser deduzido da
declaração deles?’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3.
“Kassapa, aqueles que dizem isso não falam aquilo que foi dito por mim, mas me
deturpam com algo que não é verdadeiro e é contrário aos fatos. Ocorre,
Kassapa, que eu vejo alguém que pratica mortificações e por meio do olho
divino, que é purificado e ultrapassa o humano, [<a href="#N3">3</a>] <a
name=R3></a>com a dissolução do corpo, depois da morte, eu o vejo renascer num
plano de miséria, num destino ruim, nos mundos inferiores, no inferno. Outra
vez, eu vejo alguém que pratica mortificações<span style='mso-spacerun:yes'> 
</span>... renascer num destino feliz, no paraíso. Outra vez, eu vejo alguém
que pratica poucas austeridades ... renascer num plano de miséria ... Outra
vez, eu vejo alguém que pratica poucas austeridades ... renascer num destino
feliz, no paraíso. Posto que eu posso ver como na verdade é o surgimento, o
destino, a morte e o renascimento desses contemplativos, como poderia eu
desaprovar todas as austeridades e criticar e censurar todos aqueles que vivem
dedicados à dura vida de mortificações? <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4.
“Kassapa, há certos contemplativos e Brâmanes que são sábios, hábeis, com
experiência em debates, disputadores, incisivos, rompendo em pedaços as especulações
dos seus adversários. Entre eles e eu, em relação a alguns pontos, há
concordância, mas em relação a outros pontos, não há. Algumas das coisas que
eles aprovam eu também aprovo. Algumas das coisas que eles desaprovam eu também
desaprovo. Algumas das coisas que eles aprovam eu desaprovo. Algumas das coisas
que eles desaprovam eu aprovo. Algumas das coisas que eu aprovo eles também
aprovam. E algumas das coisas que eu desaprovo eles também desaprovam. E
algumas coisas que eu aprovo eles não aprovam. E algumas coisas que eu
desaprovo eles aprovam.<span style='mso-spacerun:yes'>  </span><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5.”Aproximando-me
deles, eu digo: ‘Com relação a aquelas coisas que não estamos de acordo,
deixemo-las de lado. Com relação a aquelas coisas que estamos de acordo, que os
sábios<span style='mso-spacerun:yes'>  </span>as tomem, investiguem e analisem,
conversem com os mestres ou com os seus discípulos, dizendo: “Com relação
a aquelas coisas que são inábeis [<a href="#N4">4</a>] <a name=R4></a>e
consideradas como tal, censuráveis, que devem ser evitadas, inadequadas para um
Nobre, obscuras e consideradas como tal – quem abandonou essas coisas por
completo e está libertado delas: o contemplativo Gotama ou alguns outros
veneráveis mestres?’” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. “Então,
pode muito bem ocorrer que os sábios ... digam: ‘Com relação a aquelas coisas que
são inábeis ... o contemplativo Gotama abandonou essas coisas por completo e
está libertado delas, mas os outros veneráveis mestres abandonaram-nas apenas
em parte.’ É assim, Kassapa, que os sábios falam em nosso louvor. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. “Ou os
sábios poderão dizer: ‘Com relação a aquelas coisas que são hábeis e consideradas
como tal, não censuráveis, que devem ser praticadas, adequadas a um Nobre,
luminosas e consideradas como tal, quem é aquele que obteve completa maestria
nessas qualidades – o contemplativo Gotama ou alguns outros veneráveis
mestres?’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. “Ou os
sábios poderão dizer: ‘Com relação a aquelas coisas ... o contemplativo Gotama é
aquele que obteve completa maestria nessas qualidades, mas os outros veneráveis
mestres obtiveram apenas em parte.’ É assim, Kassapa, que os sábios falam em
nosso louvor. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9-12 (Igual
aos versos 5-8 mas substituindo ‘o contemplativo Gotama ou alguns outros
veneráveis mestres’ por ‘os discípulos do contemplativo Gotama ou ‘os discípulos
de alguns outros veneráveis mestres.’) <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13.
“Kassapa, há um caminho, há um meio que se alguém seguí-lo poderá ver e saber
por si mesmo que: ‘O contemplativo Gotama é aquele que fala no momento
apropriado, aquilo que é verdadeiro, aquilo que é benéfico [<a href="#N5">5</a>]
<a name=R5></a>– o Dhamma e a Disciplina.’ Qual é esse caminho, esse meio? É
este Nobre Caminho Óctuplo, isto é, entendimento correto, pensamento correto,
linguagem correta, ação correta, modo de vida correto, esforço correto, atenção
plena correta, concentração correta. Esse é o caminho através do qual alguém
poderá ver e saber por si mesmo que: ‘O contemplativo Gotama é aquele que fala
no momento apropriado, aquilo que é verdadeiro, aquilo que é benéfico – o
Dhamma e a Disciplina.’”<o:p></o:p></span></p>



<p class=Normal><a name=T14></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>14. Então, Kassapa disse para o Abençoado: “Gotama, estas práticas
ascéticas de certos adeptos da mortificação são por eles consideradas
apropriadas: eles andam nus, rejeitam as convenções [<a href="#N6">6</a>], <a
name=R6></a>lambem as mãos [<a href="#N7">7</a>], <a name=R7></a>não atendem
quando chamados, não param quando solicitados [<a href="#N8">8</a>]; <a
name=R8></a>eles não aceitam que lhes tragam comida [<a href="#N9">9</a>] <a
name=R9></a>ou comida feita especialmente para eles, ou convite para comer [<a
href="#N10">10</a>]; <a name=R10></a>eles não aceitam comida diretamente de um
pote ou de uma panela na qual foi cozida, ou comida colocada numa soleira, ou
colocada onde está a lenha, ou colocada onde estão os pilões, ou de duas
pessoas comendo juntas, ou de uma mulher grávida, ou de uma mulher amamentado,
ou de uma mulher num grupo com homens, ou de um lugar que tenha
divulgado a distribuição de comida, onde um cachorro esteja esperando, onde
moscas estejam zunindo; eles não aceitam peixe ou carne, eles não aceitam
bebidas alcoólicas, vinho, ou mingau de arroz fermentado. Eles se restringem a
uma casa, [<a href="#N11">11</a>] <a name=R11></a>a um bocado; eles se
restringem a duas casas, a dois bocados … eles se restringem a sete casas, a
sete bocados. Eles vivem com um pires de comida por dia, dois pires de comida
por dia … sete pires de comida por dia; Eles comem uma vez por dia, uma vez
cada dois dias … uma vez cada sete dias e assim por diante, até uma vez cada
quinze dias. Essas são consideradas práticas apropriadas.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ou eles
comem ervas ou capim, ou arroz selvagem, ou plantas aquáticas, ou farelo de
arroz, ou escuma de arroz cozido, ou flores de plantas oleaginosas, ou estrume
de vaca, ou<span style='mso-spacerun:yes'>  </span>raízes e frutas da floresta,
ou frutas caídas. Eles se vestem com cânhamo, com mortalhas, com trapos, com
casca de árvores, com pele de antílopes, com tiras de pele de antílopes, com
capim, com cabelos humanos [<a href="#N12">12</a>], <a name=R12></a>com pelos
do rabo de cavalos, com penas das asas de corujas. Eles arrancam cabelos e
barba, dedicando-se à prática de arrancar os cabelos e barba. Eles ficam em pé
continuamente, rejeitando assentos. Eles ficam de cócoras continuamente,
devotados a manter a posição de cócoras. Eles usam um colchão com espinhos;
eles fazem de um colchão com espinhos a sua cama. Eles dormem no chão. Eles
dormem sempre do mesmo lado. O corpo deles está coberto com imundície. Eles
vivem e dormem ao ar livre. Eles se alimentam de imundície, dedicando-se à
prática de comer os quatro tipos de imundície (esterco de vaca, urina de vaca,
cinzas e argila). Eles nunca bebem água fria. [<a href="#N13">13</a>] <a
name=R13></a>Eles purificam o corpo com três imersões na água a cada dia.[<a
href="#N14">14</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R14></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>15. “Kassapa, um praticante<span style='mso-spacerun:yes'>  </span>de
mortificações pode fazer todas essas coisas, mas se a sua virtude, a sua mente
e a sua sabedoria não forem desenvolvidas e cultivadas, então, de fato ele
estará muito distante de ser um contemplativo ou um Brâmane. Mas, Kassapa,
quando um bhikkhu desenvolve e cultiva uma mente plena de amor bondade,
deixando de lado a raiva e a má vontade e com a eliminação das impurezas
mentais permanece em um estado livre de impurezas com a libertação da mente e a
libertação através da sabedoria, tendo conhecido e manifestado isso para si mesmo no
aqui e agora, então, Kassapa, esse bhikkhu é chamado<span
style='mso-spacerun:yes'>  </span>um contemplativo e um Brâmane.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16. Quando
isso foi dito, Kassapa disse para o Abençoado: “Venerável Gotama, é difícil ser
um contemplativo, é difícil ser um Brâmane.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Esse é um
dito comum no mundo, Kassapa: ‘É difícil ser um contemplativo, é difícil ser um
Brâmane.’ Mas se a dificuldade, a grande dificuldade dessa vida dependesse
apenas desse ascetismo, da realização de todas as práticas que você detalhou,
então não seria apropriado dizer que a vida de um contemplativo, de um Brâmane
é difícil. Seria bem possível que um chefe de família ou o filho de um chefe de
família, ou qualquer um, até mesmo a escrava que busca água no poço, dissessem:
‘Bem, eu permanecerei nu ... (igual ao verso 14). Mas, Kassapa, porque há um
outro tipo de ascetismo muito diferente desse é correto que se diga: ‘É difícil
ser um contemplativo, é difícil ser um Brâmane.’ Mas, Kassapa, quando um
bhikkhu desenvolve e cultiva uma mente plena de amor bondade ... (igual ao
verso 15), então esse bhikkhu é chamado um contemplativo e um Brâmane.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17. Quando
isso foi dito, Kassapa disse para o Abençoado: “Venerável Gotama, é difícil
saber se alguém é um contemplativo, é difícil saber se alguém é um Brâmane.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Esse é um
dito comum no mundo, Kassapa: ‘É difícil saber se alguém é um contemplativo, é
difícil saber se alguém é um Brâmane.’ Mas se para ser um contemplativo, para
ser um Brâmane, dependesse apenas desse ascetismo, da realização de muitas das
práticas que você detalhou, então não seria apropriado dizer que um
contemplativo é difícil de ser reconhecido, um Brâmane é difícil de ser
reconhecido. Seria bem possível que um chefe de família, ou o filho de um chefe
de família, ou qualquer um, até mesmo a escrava que busca água no poço,
soubessem: ‘Esse homem permanece nu, rejeita as convenções, lambe as mãos ...’
Mas, Kassapa, porque há um outro tipo de ascetismo muito diferente desse é
correto que se diga: ‘É difícil saber se alguém é um contemplativo, é difícil
saber se alguém é um Brâmane.’<span style='mso-spacerun:yes'>  </span>Mas,
Kassapa, quando um bhikkhu desenvolve e cultiva uma mente plena de amor
bondade, deixando de lado a raiva e a má vontade, e com a eliminação das
impurezas mentais permanece num estado livre de impurezas, com a libertação da
mente e a libertação através da sabedoria, tendo conhecido e manifestado isso para si
mesmo no aqui e agora, então, Kassapa, esse bhikkhu é chamado um contemplativo
e um Brâmane.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18-20.
Então, Kassapa disse para o Abençoado: “Venerável Gotama, o que então é esse
desenvolvimento da virtude, mente e sabedoria?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Kassapa,
um Tathagata surge no mundo, um arahant, perfeitamente iluminado, consumado
no verdadeiro conhecimento e conduta, bem-aventurado, conhecedor dos mundos, um
líder insuperável de pessoas preparadas para serem treinadas, mestre de devas e
humanos, desperto, sublime. Ele declara - tendo realizado por si próprio com o
conhecimento direto - este mundo com os seus devas, maras e brahmas, esta
população com seus contemplativos e brâmanes, seus príncipes e povo. Ele ensina o
Dhamma, com o significado e fraseado corretos, que é admirável no início, admirável
no meio, admirável no final; e ele revela uma vida santa que é completamente
perfeita e imaculada. Um discípulo segue a vida santa e pratica a virtude<span
style='mso-spacerun:yes'>  </span>(veja o <a href="DN2.php#T41">DN 2, versos
41-63</a>). Essa é a perfeição da virtude. Ele guarda as portas dos seus
sentidos, etc. alcança os quatro jhanas (veja o <a href="DN2.php#T64">DN 2,
versos 64-82</a>). Essa é a perfeição da mente. Ele realiza vários insights e a
destruição das impurezas (veja o <a href="DN2.php#T83">DN 2, versos 83-98</a>).
Essa é a perfeição da sabedoria, E, Kassapa, não há nada que seja mais elevado
ou mais perfeito que essa perfeição da virtude, da mente e da sabedoria. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>21.
“Kassapa, há alguns contemplativos e Brâmanes que pregam a virtude, que é elogiada
de vários modos. Mas com relação à virtude que é nobre, a virtude superior,
eu não vejo ninguém que seja igual a mim, quanto menos superior. Eu sou o
superior com relação a isso, isto é, a virtude superior (do caminho). <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Há alguns contemplativos
e Brâmanes que pregam a mortificação e a austeridade meticulosa, que é elogiada
de vários modos. Mas com relação à mortificação e a austeridade meticulosa que
é nobre, a mortificação e a austeridade meticulosa superior, eu não vejo
ninguém que seja igual a mim e muito menos, superior. Eu sou o superior com
relação a isso, isto é, a mortificação e a austeridade meticulosa superior
(do caminho). <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Há alguns
contemplativos e Brâmanes que pregam a sabedoria, que é elogiada de vários modos.
Mas com relação à sabedoria que é nobre, a sabedoria superior, eu não vejo
ninguém que seja igual a mim e muito menos, superior. Eu sou o superior com
relação a isso, isto é, a sabedoria superior (do caminho). <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Há alguns
contemplativos e Brâmanes que pregam a libertação, que é elogiada de vários
modos. Mas com relação à libertação que é nobre, a libertação superior, eu
não vejo ninguém que seja igual a mim e muito menos, superior. Eu sou o
superior com relação a isso, isto é, a libertação superior (do caminho). <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>22.
“Kassapa, pode ser que os errantes de outras seitas digam: ‘O contemplativo
Gotama ruge o seu rugido de leão apenas em lugares vazios, não onde as pessoas
estão reunidas.’ A resposta é que isso não é verdade: ‘O contemplativo Gotama
ruge o seu rugido de leão e ele ruge onde as pessoas estão reunidas.’ Ou pode
ser que eles digam: ‘O contemplativo Gotama ruge o seu rugido de leão onde as
pessoas estão reunidas, mas ele assim o faz sem confiança.’ A resposta é que
isso não é verdade: ‘O contemplativo Gotama ruge o seu rugido de leão onde as
pessoas estão reunidas e ele assim o faz com confiança.’<span
style='mso-spacerun:yes'>  </span>Ou pode ser que eles digam: ‘O contemplativo
Gotama ruge o seu rugido de leão onde as pessoas estão reunidas e ele assim o
faz com confiança, mas elas não fazem perguntas.’ A resposta é que isso não é
verdade:<span style='mso-spacerun:yes'>  </span>‘O contemplativo Gotama ruge o
seu rugido de leão ... e elas fazem perguntas.’ Ou pode ser que eles digam:
‘... elas fazem perguntas mas ele não responde.’ ... Ou pode ser que eles
digam: ‘ ... ele responde mas não as convence com as suas respostas.’ ...<span
style='mso-spacerun:yes'>  </span>Ou pode ser que eles digam: ‘ ... mas elas
não ficam contentes.’ ... Ou pode ser que eles digam: ‘.... mas elas não ficam
satisfeitas com aquilo que ouviram.’ ... Ou pode ser que eles digam: ‘... mas
elas não se comportam como se estivessem satisfeitas.’ ... Ou pode ser que eles
digam: ‘... mas elas não se encontram no caminho para a verdade.’ ... Ou pode
ser que eles digam: ‘ ... mas elas não estão satisfeitas com a prática.’ A resposta
é que isso não é verdade: ‘O contemplativo Gotama ruge o seu rugido de leão
onde as pessoas estão reunidas e ele assim o faz com confiança, elas fazem
perguntas e ele as responde, ele as convence com as suas respostas, elas ficam
contentes e satisfeitas com aquilo que ouviram, elas se comportam como se
estivessem satisfeitas, elas estão no caminho para a verdade, elas estão
satisfeitas com a prática.’ Isso, Kassapa, é o que deve ser dito para eles. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>23. “Certa
ocasião, Kassapa, eu estava em Rajagaha no Pico do Abutre e um certo praticante
de mortificações chamado Nigrodha veio me consultar sobre a prática de
austeridades. [<a href="#N15">15</a>] <a name=R15></a>E ele ficou muito
satisfeito com a minha explicação.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, quem ao ouvir a sua explicação do Dhamma não ficaria muito satisfeito?
Eu estou muito satisfeito. Magnífico, Abençoado! Magnífico, Abençoado! O
Abençoado esclareceu o Dhamma de várias formas, como se tivesse colocado em pé
o que estava de cabeça para baixo, revelasse o que estava escondido, mostrasse
o caminho para alguém que estivesse perdido ou segurasse uma lâmpada no escuro
para aqueles que possuem visão pudessem ver as formas. Eu busco refúgio no
Abençoado, no Dhamma e na Sangha dos bhikkhus. Eu receberia a admissão na vida
santa sob o Abençoado e a admissão completa!”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>24.
“Kassapa, quem pertencia anteriormente a uma outra seita e quer ser admitido na
vida santa e a admissão completa neste Dhamma e Disciplina terá um período de
noviciado de quatro meses. Ao final dos quatro meses, se os bhikkhus estiverem
satisfeitos com ele, eles lhe darão a admissão na vida santa e também a
admissão completa como bhikkhu. Eu reconheço diferenças entre indivíduos neste
assunto.”<span style='mso-spacerun:yes'>  </span></span><span lang=PT-BR
style='mso-bidi-font-size:10.0pt;mso-ansi-language:PT-BR'><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, se esse é o caso, eu permanecerei como noviço até mesmo por quatro anos. Ao final
dos quatro anos, se os bhikkhus estiverem<span style='mso-spacerun:yes'> 
</span>satisfeitos, que me dêem a admissão na vida santa e a admissão completa
como bhikkhu&quot;. </span><span lang=PT-BR style='mso-bidi-font-size:10.0pt;
mso-ansi-language:PT-BR'><o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então,
Kassapa recebeu a admissão na vida santa sob o Abençoado e ele recebeu a
admissão completa como bhikkhu. Permanecendo só,<span
style='mso-spacerun:yes'>  </span>isolado, diligente, ardente e decidido, em
pouco tempo, o venerável Kassapa, alcançou e permaneceu no objetivo supremo da
vida santa, pelo qual membros de
um clã deixam a vida em família pela vida santa, tendo conhecido e realizado
por si mesmo no aqui e agora. Ele soube: “O nascimento foi destruído, a vida
santa foi vivida, o que deveria ser feito foi feito, não há mais vir a ser a
nenhum estado.”<span style='mso-spacerun:yes'>  </span>E assim o venerável
Kassapa tornou-se mais um dos Arahants. <o:p></o:p></span></p>

