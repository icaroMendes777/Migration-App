<p class="Normal"><span lang=PT-BR mso-ansi-language:PT-BR'>O Itivuttaka,
o quarto livro do Khuddaka Nikaya que consiste de 112 suttas curtos divididos
em quatro grupos, recebe esse nome da afirmação no começo de cada um dos
discursos: isto <i>(iti)</i> foi dito <i>(vuttam)</i> pelo Abençoado <i>(Bhagavata). </i>Essa coleção como um conjunto
é atribuída a uma discípula leiga chamada Khujjuttara, que trabalhava no
palácio do Rei Udena de Kosambi como criada de uma das suas rainhas, Samavati.
Como a Rainha não podia sair do palácio para ouvir os discursos do Buda,
Khujjuttara ía no lugar dela, memorizava aquilo que o Buda dizia e depois
regressava ao palácio para ensinar a Rainha e as suas 500 damas de companhia.
Pela sua dedicação, o Buda mencionou que Khujjuttara era a principal das suas
discípulas leigas em termos de aprendizado. Ela também foi uma professora muito
eficaz: quando os apartamentos íntimos do palácio foram mais tarde consumidos
num incêndio, matando a Rainha e as suas acompanhantes, o Buda comentou (no
Udana VII.10) que todas aquelas mulheres haviam realizado pelo menos o primeiro
estágio de iluminação.</span></p>
