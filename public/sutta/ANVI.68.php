<p class=Normal><a name=N1></a> [1] Nota do tradutor: Em geral estamos acostumados a ver nos suttas o entendimento correto, sob a forma da penetração das quatro nobres verdades, como seqüência natural da concentração correta. No <a href="MN117.php">MN 117</a> o Buda diz que o entendimento correto é o precursor do caminho Budista. Nesse caso, é o entendimento da lei de kamma que funciona como precursor. Podemos argumentar que na verdade o entendimento correto permeia todo o caminho da prática sob a forma do entendimento da causalidade. A confirmação disso está no número de vezes em que a atenção com sabedoria, (<i>yoniso manasikara</i>), é mencionada nos suttas, e em particular no que diz respeito à prática da concentração. No <a href="SNXLVI.51.php#R6">SN XLVI.51</a>, é mencionado que o alimento para a concentração é a atenção com sabedoria para o sinal da tranquilidade. [<a href="#R1">Retorna</a>]</p>

