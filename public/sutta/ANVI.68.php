<p class=Normal> “Bhikkhus, que um bhikkhu apegado ao convívio, que se delicia em  socializar-se, se delicie com o isolamento - isso não é possível. Sem estar isolado e sem a mente se deliciar com o isolamento, que a mente apreenda o sinal - isso não é possível. Quando a mente não apreende o sinal, que o bhikkhu aperfeiçoe o entendimento correto [<a href="#N1">1</a>] <a name=R1></a>– isso não é possível. Sem aperfeiçoar o entendimento correto, que ele aperfeiçoe a concentração correta [<a href="#N2">2</a>] <a name=R2></a>– isso não é possível. Sem aperfeiçoar a concentração correta, que ele destrua os grilhões [<a href="#N3">3</a>] <a name=R3></a> – isso não é possível. Sem destruir os grilhões que ele realize nibbana – isso não é possível. </p>

<p class=Normal> “Bhikkhus, que um bhikkhu não apegado ao convívio, que não se delicia em socializar-se, se delicie com o isolamento - isso é possível. Estando isolado e a mente se deliciando com o isolamento, que a mente apreenda o sinal - isso é possível. Quando a mente apreende o sinal, que o bhikkhu aperfeiçoe o entendimento correto – isso é possível. Aperfeiçoando o entendimento correto, que ele aperfeiçoe a concentração correta – isso é possível. Aperfeiçoando a concentração correta, que ele destrua os grilhões – isso é possível. Destruindo os grilhões que ele realize nibbana – isso é possível.</p>

