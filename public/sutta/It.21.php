<p class=Normal><a name=R1></a> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: "Aqui, bhikkhus, uma certa pessoa tem a mente luminosa. [<a href="#N1">1</a>] Tendo examinado a sua mente com a minha mente, eu sei que se essa pessoa viesse a morrer agora, ela iria parar no paraíso. Qual a razão disso? É porque a mente é luminosa que alguns seres com a dissolução do corpo após a morte, renascem num destino feliz, no paraíso."</p>

<p class=MsoNormal style='margin-left:.75in'> Compreendendo a mente luminosa <br> 
de uma certa pessoa aqui, <br>
o Buda explicou o significado<br>
na presença dos bhikkhus.</p>

<p class=MsoNormal style='margin-left:.75in'> Se essa pessoa viesse a morrer<br>
agora neste exato momento, <br>
ela renasceria num destino feliz <br>
devido à sua mente luminosa.</p>

<p class=MsoNormal style='margin-left:.75in'> Como se tivesse sido carregada<br>
e lá colocada, assim<br>
os seres vão para um destino feliz<br>
devido à mente luminosa</p>


