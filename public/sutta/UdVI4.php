<p class=Normal style='margin-left:.75in'>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim ouvi. Em certa
ocasião o Abençoado estava<span style='mso-spacerun:yes'>  </span>em Savatthi no
Bosque de Jeta, no Parque de Anathapindika. Agora naquela ocasião havia em
Savatthi vários brâmanes, contemplativos e errantes de várias seitas com
distintas idéias, distintas opiniões, distintas crenças e eles dependiam das
suas distintas idéias para o seu sustento. Alguns contemplativos e brâmanes
tinham este entendimento, esta doutrina: “O mundo é eterno. Somente isso é
verdadeiro; todo o restante é falso.”</span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Alguns contemplativos e
brâmanes tinham este entendimento, esta doutrina: “O mundo não é eterno” ... “O
mundo é finito” ... “O mundo é infinito” ... “A alma e o corpo são a mesma
coisa” ... “A alma é uma coisa e o corpo é outra” ... “O Tathagata existe após
a morte” ... “O Tathagata não existe após a morte” ... “O Tathagata tanto
existe como não existe após a morte” ... “O Tathagata nem existe, nem não existe
após a morte. Somente isso é verdadeiro; todo o restante é falso.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>E eles estavam assim
envolvidos em rixas e brigas, mergulhados em discussões, apunhalando uns aos
outros usando as palavras como adagas, dizendo, “O Dhamma é assim, não assado. </span>O
Dhamma não é assado, é assim.”</p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, ao amanhecer, um
grande número de bhikkhus<span style='mso-spacerun:yes'>  </span>se vestiram e
tomando a tigela e o manto externo, foram para Savatthi para esmolar alimentos.
Depois de haver esmolado em Savatthi e de haver retornado, após a refeição eles
foram até o Abençoado e depois de cumprimentá-lo sentaram a um lado e
disseram:<span style='mso-spacerun:yes'>  </span>“Venerável senhor, há<span
style='mso-spacerun:yes'>  </span>em Savatthi vários brâmanes, contemplativos e
errantes de várias seitas com distintas idéias, distintas opiniões, distintas
crenças e eles dependem das suas distintas idéias para o seu sustento ... e
eles estão envolvidos em rixas e brigas, mergulhados em discussões, apunhalando
uns aos outros usando as palavras como adagas, dizendo, ‘O Dhamma é assim, não assado.
O Dhamma não é assado, é assim.’”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus, os errantes de
outras seitas são cegos e sem visão. Eles não compreendem aquilo que é benéfico
e aquilo que é prejudicial. Eles não entendem o que é o Dhamma e o que é o
não-Dhamma. Sem compreender aquilo que é benéfico e aquilo que é prejudicial,
sem entender o que é o Dhamma e o que é o não-Dhamma, eles permanecem
envolvidos em rixas e brigas, mergulhados em discussões, apunhalando uns aos
outros usando as palavras como adagas, dizendo, ‘O Dhamma é assim, não assado. </span>O
Dhamma não é assado, é assim.’”</p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Certa vez, aqui mesmo em
Savatthi, houve um certo rei que disse para um certo homem, ‘Reúna todas as
pessoas de Savatthi que sejam cegas de nascença.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’Sim, majestade,’ o homem
respondeu e reunindo todas as pessoas de Savatthi que eram cegas de nascença,
ele foi até o rei e ao chegar disse. ‘Majestade, as pessoas de Savatthi que são
cegas de nascença foram reunidas.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’Muito bem, então
mostre-lhes um elefante.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’Sim, majestade,’ o homem
respondeu e mostrou um elefante para as pessoas cegas. Para alguns dos cegos
ele mostrou a cabeça do elefante, dizendo, ‘Isto é um elefante.’ Para outros
ele mostrou a orelha do elefante , dizendo, ‘Isto é um elefante.’ Para outros
ele mostrou uma presa … a tromba … o corpo ... um pé ... a parte traseira … o
rabo … o tufo na ponta do rabo, dizendo, ‘Isto é um elefante.’ <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então, tendo mostrado o
elefante para as pessoas cegas, o homem foi até o rei e ao chegar disse,
‘Majestade, as pessoas cegas viram o elefante. Faça aquilo que julgar
adequado.’<span style='mso-spacerun:yes'>  </span><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então o rei foi até onde
estavam as pessoas cegas e ao chegar perguntou, ‘Pessoas cegas, vocês viram o
elefante?’ <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’Sim, majestade, nós vimos
o elefante.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’Agora digam-me, pessoas
cegas, como é o elefante.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“As pessoas cegas para as
quais havia sido mostrada a cabeça do elefante responderam, ‘O elefante,
majestade, é igual a um jarro para água.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aquelas para as quais
havia sido mostrada a orelha do elefante responderam, ‘O elefante, majestade, é
igual a um cesto para trilhar.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aquelas para as quais
havia sido mostrada a presa do elefante responderam, ‘O elefante, majestade, é
igual a uma lâmina de um arado.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aquelas para as quais
havia sido mostrada a tromba do elefante responderam, ‘O elefante, majestade, é
igual à haste de um arado.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aquelas para as quais
havia sido mostrado o corpo do elefante responderam, ‘O elefante, majestade, é
igual a um celeiro.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aquelas para as quais
havia sido mostrado o pé do elefante responderam, ‘O elefante, majestade, é
igual a um poste.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aquelas para as quais
havia sido mostrada a parte traseira do elefante responderam, ‘O elefante,
majestade, é igual a uma argamassa.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aquelas para as quais
havia sido mostrado o rabo do elefante responderam, ‘O elefante, majestade, é
igual a um pilão.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aquelas para as quais
havia sido mostrado o tufo na ponta do rabo do elefante responderam, ‘O
elefante, majestade, é igual a uma vassoura.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Dizendo, ‘O elefante é
assim, não assado. O elefante não é assado, é assim,’ eles golpearam uns aos
outros com os punhos. </span>O rei se deliciou com o espetáculo.<span
style='mso-spacerun:yes'>  </span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Do mesmo modo, bhikkhus,
os errantes de outras seitas são cegos e sem visão. Eles não compreendem aquilo
que é benéfico e aquilo que é prejudicial. Eles não entendem o que é o Dhamma e
o que é o não-Dhamma. Sem compreender aquilo que é benéfico e aquilo que é
prejudicial, sem entender o que é o Dhamma e o que é o não-Dhamma, eles
permanecem envolvidos em rixas e brigas, mergulhados em discussões, apunhalando
uns aos outros usando as palavras como adagas, dizendo, ‘O Dhamma é assim, não
assado. O Dhamma não é assado, é assim.’”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, dando-se conta do
significado disso, o Abençoado nessa ocasião exclamou:<o:p></o:p></span></p>

<p class=Normal>Alguns dos assim chamados brâmanes e contemplativos, <br>
estão profundamente apegados às suas próprias idéias; <br>
as pessoas que apenas vêem um lado das coisas <br>
se envolvem em brigas e disputas. <o:p></o:p></p>

