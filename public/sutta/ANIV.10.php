<p class=Normal>"Bhikkhus, há esses quatro grilhões. Quais quatro? O grilhão dos sentidos, o grilhão de ser/existir, o grilhão das idéias, e o grilhão da ignorância. </p>

<p class=Normal> (1) "E qual, bhikkhus, é o grilhão dos sentidos? Neste caso, alguém não compreende como na verdade é a origem e a cessação, a gratificação, o perigo e a escapatória, com relação aos prazeres dos sentidos. Quando alguém não compreende essas coisas como elas na verdade são, então a cobiça sensual, o deleite sensual, a afeição sensual, a paixão sensual, a sede sensual, a obsessão sensual, o apego sensual, e o desejo sensual preenchem a mente por completo. Esse é chamado o grilhão dos sentidos. </p>

<p class=Normal> (2) "E qual é o grilhão de ser/existir? Neste caso, alguém não compreende como na verdade é a origem e a cessação, a gratificação, o perigo e a escapatória, com relação ao ser/existir. Quando alguém não compreende essas coisas como elas na verdade são, então a cobiça pelo ser/existir, o deleite pelo ser/existir, a afeição pelo ser/existir, a paixão pelo ser/existir, a sede pelo ser/existir, a obsessão pelo ser/existir, o apego pelo ser/existir, e o desejo pelo ser/existir preenchem a mente por completo. Esse é chamado o grilhão de ser/existir. </p>

<p class=Normal> (3) "E qual é o grilhão das idéias? Neste caso, alguém não compreende como na verdade é a origem e a cessação, a gratificação, o perigo e a escapatória, com relação às idéias. Quando alguém não compreende essas coisas como elas na verdade são, então a cobiça pelas idéias, o deleite pelas idéias, a afeição pelas idéias, a paixão pelas idéias, a sede pelas idéias, a obsessão pelas idéias, o apego pelas idéias, e o desejo pelas idéias preenchem a mente por completo. Esse é chamado o grilhão das idéias. </p>

<p class=Normal> (4) "E qual é o grilhão da ignorância? Neste caso, alguém não compreende como na verdade é a origem e a cessação, a gratificação, o perigo e a escapatória, com relação às seis bases do contato. Quando alguém não compreende essas coisas como elas na verdade são, então a ignorância e o desconhecimento preenchem a mente por completo. Esse é chamado o grilhão da ignorância. Esses são o grilhão dos sentidos, o grilhão de ser/existir, o grilhão das idéias, e o grilhão da ignorância." </p>

<p class=Normal>"Bhikkhus, há esses quatro rompimentos dos grilhões. Quais quatro? O rompimento do grilhão dos sentidos, o rompimento do grilhão de ser/existir, o rompimento do grilhão das idéias, e o rompimento do grilhão da ignorância. </p>

<p class=Normal> (1) "E como, bhikkhus, é o rompimento do grilhão dos sentidos? Neste caso, alguém compreende como na verdade é a origem e a cessação, a gratificação, o perigo e a escapatória, com relação aos prazeres dos sentidos. Quando alguém compreende essas coisas como elas na verdade são, então a cobiça sensual, o deleite sensual, a afeição sensual, a paixão sensual, a sede sensual, a obsessão sensual, o apego sensual, e o desejo sensual não preenchem a mente por completo. Esse é chamado o rompimento do grilhão dos sentidos. </p>

<p class=Normal> (2) "E qual é o rompimento do grilhão de ser/existir? Neste caso, alguém compreende como na verdade é a origem e a cessação, a gratificação, o perigo e a escapatória, com relação ao ser/existir. Quando alguém compreende essas coisas como elas na verdade são, então a cobiça pelo ser/existir, o deleite pelo ser/existir, a afeição pelo ser/existir, a paixão pelo ser/existir, a sede pelo ser/existir, a obsessão pelo ser/existir, o apego pelo ser/existir, e o desejo pelo ser/existir não preenchem a mente por completo. Esse é chamado o rompimento do grilhão de ser/existir. </p>

<p class=Normal> (3) "E qual é o rompimento do grilhão das idéias? Neste caso, alguém compreende como na verdade é a origem e a cessação, a gratificação, o perigo e a escapatória, com relação às idéias. Quando alguém compreende essas coisas como elas na verdade são, então a cobiça pelas idéias, o deleite pelas idéias, a afeição pelas idéias, a paixão pelas idéias, a sede pelas idéias, a obsessão pelas idéias, o apego pelas idéias, e o desejo pelas idéias não preenchem a mente por completo. Esse é chamado o rompimento do grilhão das idéias. </p>

<p class=Normal> (4) "E qual é o rompimento do grilhão da ignorância? Neste caso, alguém compreende como na verdade é a origem e a cessação, a gratificação, o perigo e a escapatória, com relação às seis bases do contato. Quando alguém compreende essas coisas como elas na verdade são, então a ignorância e o desconhecimento não preenchem a mente por completo. Esse é chamado o rompimento do grilhão da ignorância. Esses são o rompimento do grilhão dos sentidos, o rompimento do grilhão de ser/existir, o rompimento do grilhão das idéias, e o rompimento do grilhão da ignorância." </p>

<p class=Normal>"Ele está despegado dos estados ruins e prejudiciais que contaminam, que causam a renovação de ser/existir, que causam problemas, que amadurecem no sofrimento e conduzem ao futuro nascimento, envelhecimento e morte; portanto é dito que ele 'está livre do cativeiro'. Esses são os quatro rompimentos dos grilhões." </p>

<p class=Normal>Atado pelo grilhão dos sentidos<br>
e pelo grilhão do ser/existir, <br>
agrilhoado pelas idéias, <br>
precedidas pela ignorância, <br>
os seres perambulam por samsara, <br>
conduzidos para o nascimento e morte. </p>

<p class=Normal>Mas tendo compreendido por completo<br>
os prazeres dos sentidos e os grilhões do ser/existir, <br>
tendo desenraizado os grilhões das idéias<br>
e dissolvido a ignorância, <br>
os sábios romperam todos os grilhões; <br>
tendo superado o cativeiro. </p>

