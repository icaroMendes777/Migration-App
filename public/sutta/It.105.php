<p class=Normal> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: "Bhikkhus, há quatro coisas que despertam o desejo com base nas quais o desejo surge num bhikkhu. Quais são as quatro? Devido a mantos o desejo surge num bhikkhu, devido a comida esmolada o desejo surge num bhikkhu, devido a moradias o desejo surge num bhikkhu, devido a ganhar isto ou perder aquilo o desejo surge num bhikkhu. Essas bhikkhus são as quatro coisas que despertam o desejo com base nas quais o desejo surge num bhikkhu."</p>

<p class=Normal style='margin-left:.75in'> Uma pessoa em companhia do desejo<br>
perambula numa longa jornada<br>
neste estado de existência ou noutro<br>
e não consegue superar o samsara. </p>

<p class=Normal style='margin-left:.75in'> Tendo compreendido esse perigo, <br>
que o desejo é a origem do sofrimento, <br>
um bhikkhu deve permanecer com atenção plena, <br>
livre do desejo, sem apego.</p>



