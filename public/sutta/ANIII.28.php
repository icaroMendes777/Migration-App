<p class=Normal> "Bhikkhus, há três tipos de pessoas que podem ser encontradas no mundo. Quais três? Aquela cuja linguagem é como bosta, aquela cuja linguagem é como flores, e aquela cuja linguagem é como mel. </p>

<p class=Normal><a name=R1></a>"E como, bhikkhus, é a pessoa cuja linguagem é como bosta? Aqui, tendo sido chamado para uma corte, uma reunião, um encontro com seus parentes, com a sua corporação, com a família real, se assim for questionado como testemunha: 'Então, bom homem, diga o que você sabe,' se ele não souber, dirá, 'Eu sei'; se ele souber, dirá, 'Eu não sei'; se ele não viu, dirá, 'Eu vi'; se ele viu, dirá, 'Eu não vi'; com plena consciência ele conta mentiras em seu próprio benefício, pelo benefício de outros, ou para obter algum benefício mundano insignificante. [<a href="#N1">1</a>] Essa é chamada a pessoa cuja linguagem é como bosta. </p>

<p class=Normal>"E como é a pessoa cuja linguagem é como flores? Aqui, tendo sido chamado para uma corte, uma reunião, um encontro com seus parentes, com a sua corporação, com a família real, se assim for questionado como testemunha: 'Então, bom homem, diga o que você sabe,' se ele não souber, dirá, 'Eu não sei'; se ele souber, dirá, 'Eu sei'; se ele não viu, dirá, 'Eu não vi'; se ele viu, dirá, 'Eu vi'. Assim com plena consciência ele não conta mentiras em seu próprio benefício, pelo benefício de outros ou para obter algum benefício mundano insignificante. Essa é chamada a pessoa cuja linguagem é como flores. </p>

<p class=Normal>"E como é a pessoa cuja linguagem é como mel? Aqui, a pessoa abandonando a linguagem grosseira, se abstém da linguagem grosseira. Ela diz palavras que são gentis, que agradam aos ouvidos, carinhosas, que penetram o coração, que são corteses, desejadas por muitos e que agradam a muitos. Essa é chamada a pessoa cuja linguagem é como mel." </p>

<p class=Normal>"Esses, bhikkhus, são os três tipos de pessoas que podem ser encontradas no mundo."</p>

