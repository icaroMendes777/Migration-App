<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR style='mso-ansi-language:PT-BR'>Cruze a torrente, com atenção plena na percepção do nada, 
[<a href="#N2">2</a>] <a name=R2></a>apoiando-se em ‘não há nada.’ 
Abandone os prazeres sensuais, abstenha-se das conversas, busque a destruição do desejo, noite e dia. <o:p></o:p></span></p>

<p class=Normal><i style='mso-bidi-font-style:normal'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Upasiva:</span></i><span lang=PT-BR style='mso-ansi-language:PT-BR'> <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR style='mso-ansi-language:PT-BR'>Alguém livre do apego por todos prazeres sensuais, apoiando-se no nada, 
tendo deixado para trás todos os demais estados, concentrado na libertação mais elevada da percepção, ele ali permanece 
sem sujeitar-se ao samsara? [<a href="#N3">3</a>]<o:p></o:p></span></p>

<p class=Normal><i style='mso-bidi-font-style:normal'><span lang=PT-BR style='mso-ansi-language:
