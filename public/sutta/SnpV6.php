<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR style='mso-ansi-language:PT-BR'>Alguém livre do apego por todos prazeres sensuais, apoiando-se no nada, tendo deixado 
para trás todos os demais estados, concentrado na libertação mais elevada da percepção, ele ali permanece sem sujeitar-se ao samsara.<o:p></o:p></span></p>

