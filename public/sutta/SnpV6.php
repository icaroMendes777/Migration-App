<p class=Normal><b> Notas:</b><o:p></o:p></span></p>

<p class=Normal><a name=N1></a>[1] Upasiva pergunta qual objeto de meditação deve ser empregado para eliminar 
o sofrimento. [<a href="#R1">Retorna</a>]

<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[2] A esfera
do nada é um dos quatro níveis de absorção mental imateriais. A pessoa atinge
este nível após superar a esfera da consciência infinita, através da percepção,
&quot;Não há nada.&quot; O <a href="MN26.php">MN 26</a> nos conta que Alara
Kalama, o primeiro professor do Buda quando este ainda era um Bodisatva, havia alcançado
este nível de absorção mental e pensava que esse era o nível máximo que poderia
ser alcançado. O Bodisatva o deixou após dar-se conta de que essa não era a
verdadeira libertação do sofrimento. No entanto, a esfera do nada pode ser
usada como uma fase para o insight que irá conduzir à libertação [</span><a
href="#R2"><span lang=PT-BR style='mso-ansi-language:PT-BR'>Retorna</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <o:p></o:p></span></p>

<p class=Normal><a name=N3></a>[3] Upasiva quer saber se essa concentração pode ser mantida por muito tempo. [<a href="#R3">Retorna</a>]

<p class=Normal><a name=N4></a>[4] Esfriado aponta para a morte ou seja a libertação ocorrendo após a morte. 
Esse parece ser o entendimento de Upasiva – que a libertação só poderia ocorrer após a morte. [<a href="#R4">Retorna</a>]

<p class=Normal><a name=N5></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[5] Para
explorar mais a fundo esta afirmativa veja<span style='mso-spacerun:yes'> 
</span>o <a href="MN72.php">MN 72 - Aggivacchagotta Sutta - Para Vacchagotta,
sobre o Fogo</a> [<a href="#R5">Retorna</a>]<o:p></o:p></span></p>

<p class=Normal><a name=N6></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[6] Embora
Upasiva descreva o objetivo como um fenômeno (<i style='mso-bidi-font-style:
normal'>dhamma</i>), o Buda o descreve como a eliminação ou transcendência de
todos os fenômenos. “Não há nada através do qual se possa medir” também pode
ser interpretado como meios de compreender. “Meios de definição” também pode
ser interpretado como meios de linguagem. Veja também o <a href="ANIII.134.php">AN
