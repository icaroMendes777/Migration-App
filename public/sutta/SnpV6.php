<p class=Normal style='margin-left:.75in'>
Sozinho, Sakya, e sem apoio, eu não sou capaz de cruzar a grande torrente.
Diga-me, você que tudo vê, com qual apoio serei capaz de cruzar essa torrente. [<a href="#N1">1</a>]
 <o:p></o:p></span></p>

<p class=Normal><i style='mso-bidi-font-style:normal'><span lang=PT-BR style='mso-ansi-language:
