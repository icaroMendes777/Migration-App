<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. Então, um número de bhikkhus foram até o Abençoado e depois de
cumprimentá-lo sentaram a um lado e disseram:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
venerável senhor, errantes de outras seitas nos perguntam: ‘Com qual propósito,
amigos, a vida santa é vivida sob o contemplativo Gotama?’ Quando somos assim
perguntados, venerável senhor, nós respondemos a esses errantes desta forma:
‘Amigos, é para a completa compreensão do sofrimento que a vida santa é vivida
sob o Abençoado.’ Venerável senhor, esperamos que ao ter respondido dessa
forma, tenhamos dito aquilo que foi dito pelo Abençoado e que não o tenhamos
deturpado com algo contrário aos fatos. Esperamos que tenhamos explicado de
acordo com o Dhamma, de tal modo que nada que dê margem à censura possa de
forma legítima ser deduzido da nossa declaração.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Com
certeza, bhikkhus, ao terem respondido dessa forma vocês dizem aquilo que foi dito
por mim e não me deturpam com algo contrário aos fatos; vocês explicam de
acordo com o Dhamma e nada que dê margem à censura pode de forma legítima ser
deduzida da declaração de vocês. Pois, bhikkhus, é para a completa compreensão
do sofrimento que a vida santa é vivida sob mim. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se,
bhikkhus, os errantes de outras seitas perguntarem a vocês: ‘Mas, amigos, há um
caminho, há um meio para a completa compreensão desse sofrimento?’ – sendo
assim perguntados, vocês deveriam responder a esses errantes desta forma: ‘Há
um caminho, amigos, há um meio para a completa compreensão desse sofrimento.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é esse caminho, qual é esse meio para a completa compreensão do
sofrimento? É este Nobre Caminho Óctuplo; isto é, entendimento correto ... concentração
correta. Esse é o caminho, esse é o meio para a completa compreensão do
sofrimento.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sendo
assim perguntados, bhikkhus, vocês deveriam responder a esses errantes dessa
forma.”<span style='mso-tab-count:1'>            </span><o:p></o:p></span></p>



