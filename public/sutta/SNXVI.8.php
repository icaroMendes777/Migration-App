<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em Rajagaha
no Bambual. Então o Venerável Mahakassapa se aproximou do Abençoado </span>e
depois de cumprimentá-lo sentou a um lado<span lang=PT-BR style='mso-ansi-language:
PT-BR'>. O Abençoado disse: ‘Exorte os bhikkhus, Kassapa, faça um discurso do
Dhamma. Um dos dois, ou eu devo exortar os bhikkhus, Kassapa, ou você. Um dos
dois, ou eu devo fazer um discurso do Dhamma, ou você.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, os bhikkhus agora são difíceis de serem exortados, e eles possuem
qualidades que fazem com que seja difícil exortá-los. Eles são impacientes e
não aceitam as instruções com respeito.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Exatamente
assim, Kassapa, no passado os bhikkhus seniores viviam nas florestas e falavam
enaltecendo a vida nas florestas; se alimentavam de comida esmolada e falavam
enaltecendo o alimentar-se de comida esmolada; vestiam mantos feitos de trapos
e falavam enaltecendo o vestir- se com mantos feitos de trapos; vestiam três
mantos e e falavam enaltecendo o vestir-se com três mantos; possuíam poucos
desejos e falavam enaltecendo a escassez de desejos; estavam satisfeitos e
falavam enaltecendo a satisfação; viviam isolados e falavam enaltecendo o
isolamento; permaneciam afastados da sociedade e falavam enaltecendo o afastamento
da sociedade; eram energéticos e falavam enaltecendo a estimulação da energia.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
quando um bhikkhu vivia nas florestas e falava enaltecendo a vida nas florestas
... quando ele era energético e falava enaltecendo a estimulação da energia, os
bhikkhus seniores o convidavam a sentar, dizendo: ‘Venha, bhikkhu. Qual é o seu
nome? Esse bhikkhu é excelente. Esse bhikkhu tem interesse no treinamento.
Venha, bhikkhu, aqui está um assento, sente-se.’ Então os bhikkhus
recém-ordenados pensavam: ‘Parece que quando um bhikkhu vive nas florestas e
fala enaltecendo o viver nas florestas ... quando ele é energético e fala
enaltecendo a estimulação da energia, os bhikkhus seniores o convidam a
sentar.’ Eles praticavam dessa forma e isso conduzia ao bem-estar e felicidade
deles por muito tempo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas agora,
Kassapa, os bhikkhus seniores não vivem mais nas florestas e não falam
enaltecendo a vida nas florestas ... eles não são energéticos e não falam
enaltecendo a estimulação da energia. Agora são os bhikkhus conhecidos e
famosos, aqueles que ganham mantos, comida, moradia e medicamentos, que os
bhikkhu seniores convidam a sentar, dizendo: Venha, bhikkhu. Qual é o seu nome?
Esse bhikkhu é excelente. Esse bhikkhu tem interesse em estar na companhia dos
seus irmãos na vida santa. Venha, bhikkhu, aqui está um assento, sente-se.’
Então os bhikkhus recém-ordenados pensam: ‘Parece que quando um bhikkhu é bem
conhecido e famoso, alguém que ganha mantos, comida, moradia e medicamentos, os
bhikkhus seniores o convidam a sentar.’ Eles praticam dessa forma e isso será
para o dano e sofrimento deles por muito tempo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Kassapa,
alguém falando corretamente poderia dizer: ‘Aqueles que vivem a vida santa
foram arruinados pela ruína daqueles que lideram a vida santa; aqueles que
vivem a vida santa foram derrotados pela derrota daqueles que lideram a vida
santa,’ é unicamente desse modo que alguém poderia corretamente dizer isso.”<b
style='mso-bidi-font-weight:normal'><o:p></o:p></b></span></p>


