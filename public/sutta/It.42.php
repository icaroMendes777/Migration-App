<p class=Normal> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: "Bhikkhus, essas duas condições puras protegem o mundo. Quais duas? A vergonha e o temor de cometer transgressões. Bhikkhus, se essas duas condições puras não protegerem o mundo então não seria discernido o respeito pela mãe, ou pela tia, ou pela cunhada da mãe, ou pela esposa do mestre, ou pela esposa do pai de alguém, e assim por diante – haveria completa promiscuidade no mundo igual a bodes e cabras, porcos e porcas, cães e chacais. Mas como essas duas condições puras protegem o mundo, há o discernimento do respeito pela mãe ... e assim por diante."</p>

<p class=Normal style='margin-left:.75in'> Naqueles em que a vergonha e o temor de cometer transgressões<br>
não são encontrados de forma consistente<br>
desviaram-se do bom caminho<br>
conduzidos a um novo nascimento e morte.</p>

<p class=Normal style='margin-left:.75in'> Mas naqueles em que a vergonha e o temor de cometer transgressões<br>
são encontrados de forma consistente<br>
pacificados, tendo cultivado a vida santa, <br>
dão um fim à renovação do ser/existir.</p>


