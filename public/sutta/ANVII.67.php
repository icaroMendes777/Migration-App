<p class=Normal> “Bhikkhus, embora um bhikkhu que não se dedique ao desenvolvimento meditativo da sua mente possa desejar, ‘Ah! Que a minha mente possa se libertar das impurezas através da ausência de apego!’ no entanto, a mente dele não se libertará. Por qual motivo? ‘Porque ele não desenvolveu a sua mente,’ deve ser dito. Não desenvolveu a mente de que modo? Nos quatro fundamentos da atenção plena, nos quatro esforços corretos, nas quatro bases do poder espiritual, nas cinco faculdades, nos cinco poderes, nos sete fatores da Iluminação, no nobre caminho óctuplo. </p> 

<p class=Normal> “Suponham, bhikkhus, que uma galinha tenha oito, dez ou doze ovos. Se ela não cobrí-los corretamente, aquecê-los corretamente, incubá-los corretamente, então mesmo que ela tenha o desejo, ‘Que as minhas crias rompam as cascas dos ovos com suas garras afiadas ou bicos e saiam dos ovos com segurança!’ ainda assim não será possível que os pintos rompam as cascas dos ovos com suas garras afiadas ou bicos e saiam dos ovos com segurança. Por que ocorre isto? Porque a galinha não cobriu-os corretamente, não aqueceu-os corretamente, não incubou-os corretamente. Da mesma maneira ocorre com um bhikkhu que não se dedica ao desenvolvimento meditativo da sua mente. </p>

<p class=Normal> “Se, no entanto, um bhikkhu que se dedicou ao desenvolvimento meditativo da sua mente, mesmo que ele não deseje, ‘Ah! Que a minha mente possa se libertar das impurezas através da ausência de apego!’ ainda assim,  a mente dele se libertará. Por qual motivo? ‘Porque ele desenvolveu a sua mente,’ deve ser dito. Desenvolveu a mente de que modo? Nos quatro fundamentos da atenção plena, nos quatro esforços corretos, nas quatro bases do poder espiritual, nas cinco faculdades, nos cinco poderes, nos sete fatores da Iluminação, no nobre caminho óctuplo. </p>

<p class=Normal> “Suponham, bhikkhus, que uma galinha tenha oito, dez ou doze ovos que ela cobriu corretamente, aqueceu corretamente, incubou corretamente. Mesmo que este desejo não lhe ocorra, ‘Que as minhas crias rompam as cascas dos ovos com suas garras afiadas ou bicos e saiam dos ovos com segurança!’ ainda assim será possível que os pintos rompam as cascas dos ovos com suas garras afiadas ou bicos e saiam dos ovos com segurança. Por que ocorre isto? Porque a galinha cobriu-os corretamente, aqueceu-os corretamente, incubou-os corretamente. Da mesma maneira ocorre com um bhikkhu que se dedica ao desenvolvimento meditativo da sua mente. </p>

<p class=Normal> “Suponham, bhikkhus, que um carpinteiro ou aprendiz de carpinteiro vê as marcas de seus dedos, ou do dedão, no cabo do seu enxó, embora sem saber, ‘Hoje o cabo do meu enxó foi gasto este tanto, ou ontem ele foi gasto outro tanto, ou anteontem ele foi gasto outro tanto,’ quando estiver gasto ele saberá que está gasto. De modo semelhante ocorre com um bhikkhu que se dedica ao desenvolvimento meditativo da sua mente: embora ele não saiba, ‘Hoje minhas impurezas gastaram este tanto, ou ontem elas gastaram um outro tanto, ou anteontem elas gastaram um outro tanto,’ quando elas estiverem gastas ele saberá que estão gastas. </p>

<p class=Normal> “Suponham bhikkhus, que um navio transoceânico, equipado com mastros e cordame, desgastado após seis meses na água, fosse levado para uma doca seca durante o período do inverno, o seu cordame exposto ao calor e ao vento, umedecido pelas nuvens da estação das chuvas, facilmente encolheria e apodreceria. Assim também, ocorre com um bhikkhu que se dedica ao desenvolvimento meditativo da sua mente, os seus grilhões facilmente encolhem e apodrecem.”  </p>

