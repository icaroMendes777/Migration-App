<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião o Abençoado estava entre os Anguttarapas numa cidade
denominada Apana.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. Então,
ao amanhecer, o Abençoado se vestiu e tomando a tigela e o manto externo, foi
para Apana para esmolar alimentos. Depois de haver esmolado em Apana e de haver
retornado, após a refeição ele foi até um certo bosque para passar o resto do
dia. Tendo entrado no bosque, ele sentou à sombra de uma árvore.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. Ao
amanhecer, o venerável Udayin se vestiu e tomando a tigela e o manto externo,
ele também foi para Apana para esmolar alimentos. Depois de haver esmolado em
Apana e de haver retornado, após a refeição ele foi até o mesmo bosque para
passar o resto do dia. Tendo entrado no bosque, ele sentou à sombra de uma árvore.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. Então,
enquanto o venerável Udayin estava só em meditação, o seguinte pensamento lhe
ocorreu:<span style="mso-spacerun: yes">  </span>“De quantos estados aflitivos
o Abençoado nos libertou! Quantos estados prazerosos o Abençoado nos trouxe! De
quantos estados prejudiciais o Abençoado nos libertou! Quantos estados benéficos
o Abençoado nos trouxe!”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. Então,
ao anoitecer, o venerável Udayin se levantou da meditação e foi até o Abençoado
e depois de cumprimentá-lo sentou a um lado e disse:<o:p></o:p></span></p>



<p class=Normal><a name=T6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>6. “Aqui, venerável senhor, enquanto eu estava só em meditação, o
seguinte pensamento me ocorreu:<span style="mso-spacerun: yes">  </span>‘De
quantos estados aflitivos o Abençoado nos libertou! ... Quantos estados
benéficos o Abençoado nos trouxe!’ Venerável senhor, no passado costumávamos
comer à noite, pela manhã e durante o dia, fora do horário apropriado. Então,
houve uma ocasião em que o Abençoado se dirigiu aos bhikkhus da seguinte forma:
‘Bhikkhus por favor abandonem essa refeição durante o dia, fora do horário
apropriado.’ [<a href="#N1">1</a>] <a name=R1></a>Venerável senhor, eu fiquei
preocupado e triste, pensando: ‘Chefes de Família dedicados nos dão bons
alimentos de vários tipos durante o dia, fora do horário apropriado, no
entanto, o Abençoado nos diz para abandonar isso, o Sublime nos diz para
renunciar a isso.’ Com base no amor e respeito pelo Abençoado e pelo temor e
vergonha de cometer uma transgressão, nós abandonamos aquela refeição durante o
dia, fora do horário apropriado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então
apenas comíamos à noite e pela manhã. Então houve uma ocasião em que o
Abençoado se dirigiu aos bhikkhus da seguinte forma: ‘Bhikkhus por favor
abandonem essa refeição noturna, que está fora do horário apropriado.’
Venerável senhor, eu fiquei preocupado e triste, pensando: ‘O Abençoado nos diz
para abandonar a mais suntuosa dentre as nossas duas refeições, o Sublime nos
diz para renunciá-la.’ Certa vez, venerável senhor, um certo homem havia
ganhado um tanto de sopa durante o dia e ele disse: ‘Coloquemos isso de lado
para que comamos juntos à noite.’ [Quase] toda cocção é feita à noite, pouca
durante o dia. Com base no amor e respeito pelo Abençoado e pelo temor e
vergonha de cometer uma transgressão, nós abandonamos aquela refeição à noite
fora do horário apropriado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Já
aconteceu, venerável senhor, que bhikkhus perambulando por alimentos no negrume
da noite acabaram caindo numa fossa, num esgoto, se meteram num arbusto
espinhoso e tropeçaram numa vaca dormente; eles encontraram criminosos que já
haviam cometido crime e aqueles que estavam planejando cometê-lo, e eles foram
seduzidos por mulheres. Certa vez, venerável senhor, eu fui perambular por
alimentos no negrume da noite. Uma mulher lavando um pote me viu através do
clarão de um relâmpago e gritou horrorizada: ‘Piedade, o diabo veio me buscar!’
Eu lhe disse: ‘Irmã, eu não sou o diabo, eu sou um bhikkhu perambulando por
alimentos’ – ‘Então é um bhikkhu cuja mãe e pai faleceram! [<a href="#N2">2</a>]
<a name=R2></a>Melhor, bhikkhu, que você tenha a sua barriga cortada por uma
faca afiada de açougueiro do que essa ronda por esmolas no negrume da noite em
benefício da sua barriga!’ Venerável senhor, quando recordo disso eu penso: ‘De
quantos estados aflitivos o Abençoado nos libertou! Quantos estados prazerosos
o Abençoado nos trouxe! De quantos estados prejudiciais o Abençoado nos
libertou! Quantos estados benéficos o Abençoado nos trouxe!’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. “Da
mesma forma, Udayin, aqui há certos homens tolos e quando lhes digo, ‘Abandonem
isso,’ dizem: ‘O quê, essa mera trivialidade, uma coisa tão insignificante como
essa? Esse contemplativo é exigente demais!’ E eles não abandonam isso, eles demonstram
descortesia em relação a mim bem como em relação àqueles bhikkhus que desejam
ser treinados. Para eles, essa coisa se torna um atilho forte, grosso, durável
e resistente, e um<span style="mso-spacerun: yes">  </span>grilhão pesado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8.
“Suponha, Udayin, que uma codorna estivesse atada por uma trepadeira apodrecida e
devido a isso ela pudesse esperar uma lesão, cativeiro ou morte. Agora suponha
que alguém dissesse: ‘A trepadeira apodrecida pela qual essa codorna está atada,
sendo que por causa dela ela pode esperar uma lesão, cativeiro ou morte, é para
ela um atilho débil, fraco, podrido e vazio.’ Ele estaria falando
corretamente?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
venerável senhor. Pois, para aquela codorna, a trepadeira apodrecida pela qual ela
está atada, sendo que por causa dela ela pode esperar uma lesão, cativeiro ou
morte, é um atilho forte, grosso, durável e resistente, e um grilhão pesado.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Da mesma
forma, Udayin, aqui há certos homens tolos e<span style="mso-spacerun: yes"> 
</span>quando lhes digo ‘Abandonem isso,’ ... não abandonam, eles demonstram
descortesia em relação a mim bem como em relação àqueles bhikkhus que desejam
ser treinados. Para eles, essa coisa se torna um atilho forte, grosso, durável
e resistente e um grilhão pesado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. “Udayin,
aqui há certos membros de clãs e quando lhes digo ‘Abandonem isso,’ dizem: ‘O
quê, essa mera trivialidade, uma coisa tão insignificante como essa o Abençoado
nos diz para abandonar, o Sublime nos diz para renunciar.’ No entanto, eles
abandonam isso e não demonstram descortesia em relação a mim, nem em relação
àqueles bhikkhus que desejam ser treinados. Ao abandonar isso, vivem em paz,
serenos, subsistindo daquilo que os outros dão, permanecendo com a mente
[desapegada] como um gamo selvagem. Para eles, essa coisa se torna um atilho
débil, fraco, podrido e vazio.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10.
“Suponha, Udayin, que um elefante real com as presas tão longas quanto os eixos
de uma carruagem, maduro em tamanho, com bom pedigree e acostumado às batalhas,
fosse atado com fortes correias de couro, mas que só com uma pequena torcida do
corpo ele pudesse romper e arrebentar as correias e depois pudesse ir aonde ele
desejasse. Agora, suponha que alguém dissesse: ‘As fortes correias de couro
pelas quais este elefante real está atado ... são para ele um atilho forte,
grosso, durável e resistente, e um grilhão pesado.’ Ele estaria falando da
forma correta?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
venerável senhor. As fortes correias de couro pelas quais aquele elefante real
está atado e que só com uma pequena torcida do corpo ele poderia rompê-las e
arrebentá-las e depois poderia ir aonde ele desejasse, são para ele um atilho
débil, fraco, podrido e vazio.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Da mesma
forma, Udayin, aqui há certos membros de clãs, e quando lhes digo ‘Abandonem
isso,’ ... abandonam isso, eles não demonstram descortesia em relação a mim, nem
em relação àqueles bhikkhus que desejam ser treinados. Ao abandonar<span
style="mso-spacerun: yes">  </span>isso, vivem em paz, serenos, subsistindo
daquilo que os outros dão, permanecendo com a mente [desapegada] como um gamo
selvagem. Para eles, essa coisa se torna um atilho débil, fraco, podrido e
vazio.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11.
“Suponha, Udayin, que houvesse um homem pobre, sem dinheiro, destituído e que
ele tivesse uma cabana dilapidada aberta aos corvos, não do melhor tipo, e um
estrado trançado dilapidado, não do melhor tipo, e um pouco de grãos e sementes
de abóbora num pote, não do melhor tipo, e uma bruxa como esposa, não do melhor
tipo. Pode ser que ele visse um bhikkhu num parque dum monastério sentado à
sombra de uma árvore, com as mãos e os pés bem lavados depois de ter comido uma
refeição deliciosa, dedicando-se à mente superior. Ele poderia pensar: ‘Que
prazerosa é a situação daquele contemplativo! Que saudável é a situação daquele
contemplativo! Se eu pudesse raspar o meu cabelo e barba, vestir o manto de cor
ocre e deixar a vida em família para seguir a vida santa!’ Mas sendo incapaz de
abandonar a sua cabana dilapidada aberta aos corvos, não do melhor tipo, incapaz de
abandonar o
seu estrado trançado dilapidado, não do melhor tipo, incapaz de
abandonar o seu pouco de grãos e
sementes de abóbora num pote, não do melhor tipo, incapaz de
abandonar a sua bruxa como esposa,
não do melhor tipo, ele é incapaz de raspar o cabelo e barba, vestir o manto de
cor ocre e deixar a vida em família para seguir a vida santa. Agora, suponha
que alguém dissesse: ‘Os atilhos pelos quais aquele homem está atado, de modo
que ele não é capaz de abandonar a sua cabana dilapidada ... e a sua bruxa como
esposa, não do melhor tipo, e de raspar o cabelo e barba, vestir o manto de cor
ocre e deixar a vida em família para seguir a vida santa – para ele, eles são
um atilho débil, fraco, podrido e vazio.’ Ele estaria falando da forma
correta?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não, venerável
senhor. ‘Os atilhos pelos quais aquele homem está atado, de modo que ele não é
capaz de abandonar a sua cabana dilapidada ... e a sua bruxa como esposa, não
do melhor tipo, e de raspar o cabelo e barba, vestir o manto de cor ocre e
deixar a vida em família para seguir a vida santa – para ele, eles são um
atilho forte, grosso, durável e resistente, e um grilhão pesado.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Da mesma
forma, Udayin, aqui há certos homens tolos e quando lhes digo ‘Abandonem isso,’
... não abandonam, eles demonstram descortesia em relação a mim bem como em
relação àqueles bhikkhus que desejam ser treinados. Para eles, essa coisa se
torna um atilho forte, grosso, durável e resistente, e um grilhão pesado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12.
“Suponha, Udayin, que houvesse um rico chefe de familia ou filho de um chefe de
familia, com grande riqueza e posses, com uma grande quantidade de barras de
ouro, uma grande quantidade de celeiros, uma grande quantidade de campos, uma
grande extensão de terras, inúmeras esposas e uma grande quantidade de escravos
e escravas. Pode ser que ele visse um bhikkhu num parque dum monastério sentado
à sombra de uma árvore, com as mãos e os pés bem lavados depois de ter comido
uma refeição deliciosa, dedicando-se à mente superior. Ele poderia pensar: ‘Que
prazerosa é a situação daquele contemplativo! Que saudável é a situação daquele
contemplativo! Se eu pudesse raspar o meu cabelo e barba, vestir o manto de cor
ocre e deixar a vida em família para seguir a vida santa!’ E sendo capaz de
abandonar as suas inúmeras barras de ouro, abandonar os seus inúmeros celeiros, abandonar os seus
inúmeros campos, abandonar a sua grande extensão de terras, abandonar as suas inúmeras esposas e abandonar os
seus inúmeros escravos e escravas, ele é capaz de raspar o seu cabelo e barba,
vestir o manto de cor ocre e deixar a vida em família para seguir a vida santa.
Agora, suponha que alguém dissesse: ‘Os atilhos pelos quais aquele homem está
atado, de modo que ele é capaz de abandonar as suas inúmeras barras de ouro ...
os seus inúmeros escravos e escravas, e raspar o seu cabelo e barba, vestir o manto
de cor ocre e deixar a vida em família para seguir a vida santa - para ele,
eles são um atilho forte, grosso, durável e resistente.’ Ele estaria falando da
forma correta?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
venerável senhor. ‘Os atilhos pelos quais aquele homem está atado, de modo que
ele é capaz de abandonar as suas inúmeras barras de ouro ... os seus inúmeros
escravos e escravas, e raspar o seu cabelo e barba, vestir o manto de cor ocre
e deixar a vida em família para seguir a vida santa - para ele, eles são um
atilho débil, fraco, podrido e vazio.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Da mesma
forma, Udayin, aqui há certos membros de clãs, e quando lhes digo ‘Abandonem
isso,’ ... abandonam isso, eles não demonstram descortesia em relação a mim, nem
em relação àqueles bhikkhus que desejam ser treinados. Ao abandonar isso, vivem
em paz, serenos, subsistindo daquilo que os outros dão, permanecendo com a
mente [desapegada] como um gamo selvagem. Para eles, essa coisa se torna um atilho
débil, fraco, podrido e vazio.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. “Udayin,
existem quatro tipos de pessoas que podem ser encontradas no mundo. Quais
quatro?[<a href="#N3">3</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>14. “Aqui, Udayin, certa pessoa pratica o caminho para o abandono das
aquisições, para a renúncia às aquisições. [<a href="#N4">4</a>] <a name=R4></a>Enquanto
ela está assim praticando, memórias e intenções associadas com as aquisições a
incomodam. Ela as tolera; ela não as abandona, não as remove, não as elimina e
não as aniquila. Tal pessoa eu chamo de presa por grilhões, não livre de
grilhões. Por que isso? Porque eu conheço a diversidade particular das
faculdades nessa pessoa. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. “Aqui,
Udayin, certa pessoa pratica o caminho para o abandono das aquisições para a
renúncia às aquisições. Enquanto ela está assim praticando, memórias e intenções
associadas com as aquisições a incomodam. Ela não as tolera; ela as abandona,
remove, elimina e aniquila. Tal pessoa também, eu chamo de presa por grilhões,
não livre de grilhões. Por que isso? Porque eu conheço a diversidade particular
das faculdades nessa pessoa.[<a href="#N5">5</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R5></a><a name=T16></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>16. “Aqui, Udayin, certa pessoa pratica o
caminho para o abandono das aquisições para a renúncia às aquisições. Enquanto
ela está assim praticando, memórias e intenções associadas com as aquisições a
incomodam de vez em quando devido a lapsos na atenção plena. A sua atenção
plena pode ser lenta no seu surgimento, mas ela rapidamente as abandona,
remove, elimina e aniquila. [<a href="#N6">6</a>] <a name=R6></a>Como se um
homem deixasse cair duas ou três gotas d'água sobre uma chapa de ferro aquecida
durante todo um dia, a queda das gotas poderia ser lenta mas elas se
vaporizariam rapidamente e desapareceriam. Da mesma forma, aqui certa pessoa
pratica o caminho ... A sua atenção plena pode ser lenta no seu surgimento, mas
ela rapidamente as abandona, remove, elimina e aniquila. Tal pessoa eu também
chamo de presa por grilhões, não livre de grilhões. Por que isso? Porque eu
conheço a diversidade particular das faculdades nessa pessoa.<o:p></o:p></span></p>



<p class=Normal><a name=T17></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>17. “Aqui, Udayin, certa pessoa, tendo compreendido que as aquisições
são a raiz do sofrimento, se livra das aquisições e é libertada através da
destruição das aquisições. Tal pessoa eu chamo de livre de grilhões e não presa
por grilhões.[<a href="#N7">7</a>] <a name=R7></a>Por que isso? Porque eu
conheço a diversidade particular das faculdades nessa pessoa.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18.
“Existem, Udayin, esses cinco elementos do prazer sensual. Quais cinco? Formas
conscientizadas através do olho que são desejáveis, agradáveis e fáceis de serem
gostadas, conectadas com o desejo sensual e que provocam a cobiça. Sons
conscientizados através do ouvido ... Aromas conscientizados através do nariz ... Sabores
conscientizados através da língua ... Tangíveis conscientizados através do corpo que são
desejáveis, agradáveis e fáceis de serem gostados, conectados com o desejo
sensual e que provocam a cobiça. Esses são os cinco elementos do prazer
sensual.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>19. “Agora,
Udayin, o prazer e a alegria que surge na dependência desses cinco elementos do
prazer sensual é chamado de prazer sensual – um prazer baixo, um prazer
grosseiro, um prazer ignóbil. Eu digo desse tipo de prazer que não deve ser
buscado, não deve ser desenvolvido, não deve ser cultivado e que deve ser
temido.<o:p></o:p></span></p>



<p class=Normal><a name=T20></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>20. “Agora, Udayin, afastado dos prazeres sensuais, afastado das
qualidades não hábeis, um bhikkhu entra e permanece no primeiro jhana ...
abandonando o pensamento aplicado e sustentado, um bhikkhu entra e permanece no
segundo jhana ... abandonando o êxtase ... um bhikkhu entra e permanece
no terceiro jhana ... com o completo desaparecimento da felicidade ... um
bhikkhu entra e permanece no quarto jhana...<o:p></o:p></span></p>



<p class=Normal><a name=T21></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>21. “Isto é chamado de felicidade da renúncia, a felicidade do afastamento,
a felicidade da paz, a felicidade da iluminação. [<a href="#N8">8</a>] <a
name=R8></a>Eu digo desse tipo de prazer que ele deve ser buscado, deve ser
desenvolvido, deve ser cultivado e que não deve ser temido.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>22. “Aqui,
Udayin, afastado dos prazeres sensuais, afastado das qualidades não hábeis, um
bhikkhu entra e permanece no primeiro jhana ... Agora isso, eu digo, faz parte
do perturbável. [<a href="#N9">9</a>] <a name=R9></a>E nisso, o que faz parte
do perturbável? O pensamento aplicado e sustentado que ainda não cessou, isso é
o que faz parte do perturbável. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>23. “Aqui,
Udayin, abandonando o pensamento aplicado e sustentado, um bhikkhu entra e
permanece no segundo jhana ... Agora isto, eu digo, também faz parte do
perturbável. E nisso, o que faz parte do perturbável? O êxtase e a felicidade
que ainda não cessaram, isso é o que faz parte do perturbável.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>24. “Aqui,
Udayin, abandonando o êxtase ... um bhikkhu entra e permanece no
terceiro jhana ... Agora isto, eu digo, também faz parte do perturbável. E
nisso, o que faz parte do perturbável? A equanimidade e a felicidade que ainda
não cessaram, isso é o que faz parte do perturbável.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>25. “Aqui,
Udayin, com o completo desaparecimento da felicidade ... um bhikkhu entra e
permanece no quarto jhana ... Agora isto, eu digo, faz parte do imperturbável.<o:p></o:p></span></p>



<p class=Normal><a name=T26></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>26. “Aqui, Udayin, afastado dos prazeres sensuais, afastado das
qualidades não hábeis, um bhikkhu entra e permanece no primeiro jhana ... Isso,
eu digo, não é o bastante. [<a href="#N10">10</a>] <a name=R10></a>Abandone
isso, eu digo; supere isso, eu digo. E o que supera isso?<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>27. “Aqui,
Udayin, abandonando o pensamento aplicado e sustentado, um bhikkhu entra e
permanece no segundo jhana ... Isso supera aquilo. Mas isso também, eu digo,
não é o bastante. Abandone isso, eu digo; supere isso, eu digo. E o que supera
isso?<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>28. “Aqui, Udayin,
abandonando o êxtase ... um bhikkhu entra e permanece no terceiro jhana
... Isso supera aquilo. Mas isso também, eu digo, não é o bastante. Abandone
isso, eu digo; supere isso, eu digo. E o que supera isso?<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>29. “Aqui,
Udayin, com o completo desaparecimento da felicidade ... um bhikkhu entra e
permanece no quarto jhana ... Isso supera aquilo. Mas isso também, eu digo, não
é o bastante. Abandone isso, eu digo; supere isso, eu digo. E o que supera
isso?<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>30. “Aqui,
Udayin, com a completa superação das percepções da forma, com o desaparecimento
das percepções do contato sensorial, sem dar atenção às percepções da
diversidade, consciente de que o ‘espaço é infinito,’ um bhikkhu entra e
permanece na base do espaço infinito. Isso supera aquilo. Mas isso também, eu
digo, não é o bastante. Abandone isso, eu digo; supere isso, eu digo. E o que
supera isso?<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>31. “Aqui,
Udayin, com a completa superação da base do espaço infinito, consciente de que
a ‘consciência é infinita,' um bhikkhu entra e permanece na base da consciência
infinita. Isso supera aquilo. Mas isso também, eu digo, não é o bastante.
Abandone isso, eu digo; supere isso, eu digo. E o que supera isso?<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>32. “Aqui,
Udayin, com a completa superação da base da consciência infinita, consciente de
que ‘não há nada,' um bhikkhu entra e permanece na base do nada. Isso supera
aquilo. Mas isso também, eu digo, não é o bastante. Abandone isso, eu digo;
supere isso, eu digo. E o que supera isso?<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>33. “Aqui,
Udayin, com a completa superação da base do nada, um bhikkhu entra e permanece
na base da nem percepção, nem não percepção. Isso supera aquilo. Mas isso
também, eu digo, não é o bastante. Abandone isso, eu digo; supere isso, eu
digo. E o que supera isso?<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>34. “Aqui,
Udayin, com a completa superação da base da nem percepção, nem não percepção,
um bhikkhu entra e permanece na cessação da percepção e sensação. [<a
href="#N11">11</a>] <a name=R11></a>Isso supera aquilo. Portanto, eu digo, para
abandonar até mesmo a base da nem percepção, nem não percepção. Você vê,
Udayin, algum grilhão, pequeno ou grande, cujo abandono eu não afirme?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
venerável senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o
que disse o Abençoado. O venerável Udayin ficou satisfeito e contente com as
palavras do Abençoado.<o:p></o:p></span></p>


