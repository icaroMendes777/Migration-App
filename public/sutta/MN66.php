<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[1] Este trecho e mais o que segue dão a
impressão de que o Buda restringiu o horário permitido para as refeições dos
bhikkhus em dois estágios sucessivos, primeiro proibindo apenas a refeição da
tarde e permitindo uma refeição à noite. No entanto, no relato contido no
Vinaya não há nenhuma menção a essa proibição sucessiva. Ao contrário, o texto
parece assumir ser de conhecimento público que os bhikkhus não devem consumir
alimentos depois do meio dia e mostra o Buda estabelecendo a regra contra as
refeições fora do horário adequado através de um pronunciamento válido para
todas as refeições após o meio dia. [<a href="#R1">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
