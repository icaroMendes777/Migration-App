<p class=Normal><a name=N4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[4] <i>Upadhi</i>. MA explica: Para o abandono
de quatro tipos de <i>upadhi</i> – os agregados, contaminações, formações
volitivas e elementos do prazer sensual (<i>khandh'upadhi kiles' upadhi
abhisankhar' upadhi kamagun' upadhi</i>). [<a href="#R4">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N5></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[5] MA: A pessoa comum, aquele que entrou na
correnteza, aquele que retorna uma vez e aquele que não retorna, podem todos
ser incluídos sob a primeira categoria (verso 14); aquele que não retorna,
porque o desejo por existir ainda existe nele e portanto, de vez em quando ele
pode se deliciar com os pensamentos dos prazeres mundanos. Os mesmos quatro
podem ser incluídos na segunda categoria (verso 15), a pessoa comum, porque ela
pode suprimir as contaminações que surgiram, despertar a energia, desenvolver o
insight e erradicar as contaminações ao alcançar o caminho supramundano. [<a
href="#R5">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[6] Este tipo se distingue do tipo anterior
apenas pela sua lentidão em despertar a atenção plena para abandonar as
contaminações que já surgiram. [<a href="#R6">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N7></a><span lang=PT-BR style='mso-ansi-language:
