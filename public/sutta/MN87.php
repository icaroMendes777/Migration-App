<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no
Parque de Anathapindika.<o:p></o:p></span></p>



<p class=Normal>2. Agora, naquela ocasião o único filho amado e querido de
um certo chefe de família havia morrido. Depois da morte do filho, ele não
tinha mais vontade de trabalhar ou de comer. Ele ia ao cemitério e chorava:
“Meu único filho, onde você está? Meu único filho, onde você está?”</p>


<p class=Normal><a name=T3></a>3. Então, aquele chefe de família foi até o Abençoado e
depois de cumprimentá-lo sentou a um lado. O Abençoado disse: “Chefe de
família, as suas faculdades não são aquelas de alguém com controle sobre a
própria mente. As suas faculdades estão perturbadas.”</p>

<p class=Normal>“Como poderiam as minhas faculdades não estarem perturbadas,
venerável senhor? Pois o meu único filho, amado e querido, morreu. Desde que
ele morreu eu não tenho mais vontade de trabalhar ou de comer. Eu vou ao
cemitério e choro: ‘Meu único filho, onde você está? Meu único filho, onde você
está?’”</p>

<p class=Normal>“Assim é, chefe de família, assim é! Tristeza, lamentação,
dor, angústia e desespero nascem daqueles que amamos, surgem daqueles que
amamos.”</p>

<p class=Normal>“Venerável senhor, quem alguma vez pensaria que a tristeza,
lamentação, dor, angústia e desespero nascem daqueles que amamos, surgem
daqueles que amamos? Venerável senhor, a alegria e a felicidade nascem daqueles
que amamos, surgem daqueles que amamos.” Então, descontente com as palavras do
Abençoado, desaprovando-as, o chefe de família se levantou do seu assento e
partiu.</p>



<p class=Normal>4. Agora, naquela ocasião alguns jogadores estavam jogando
com os dados não muito distante do Abençoado. Então, o chefe de família foi até
aqueles jogadores e disse: “Bem agora, senhores, eu fui até o contemplativo
Gotama e depois de cumprimentá-lo sentei a um lado e o contemplativo Gotama
disse: ‘Chefe de família, as suas faculdades não são aquelas de alguém com
controle sobre a própria mente’ ... (igual ao verso 3) ... ‘Venerável senhor, a
alegria e a felicidade nascem daqueles que amamos, surgem daqueles que amamos.’
Então, descontente com as palavras do Abençoado, desaprovando-as, eu me
levantei do assento e parti.”</p>

<p class=Normal>“Assim é, chefe de família, assim é! A alegria e a
felicidade nascem daqueles que amamos, surgem daqueles que amamos.”</p>

<p class=Normal>Então o chefe de família partiu pensando: “Eu concordo com
os jogadores.”</p>



<p class=Normal>5. No final das contas esta história chegou ao palácio real.
Então, o Rei Pasenadi de Kosala disse para a Rainha Mallika: “Isso é o que foi
dito pelo contemplativo Gotama, Mallika: ‘Tristeza, lamentação, dor, angústia e
desespero nascem daqueles que amamos, surgem daqueles que amamos.’”</p>

<p class=Normal>“Se isso foi dito pelo Abençoado, senhor, então assim é.”</p>

<p class=Normal>“Não importa o que o contemplativo Gotama diga, Mallika
aplaude o contemplativo desta forma: ‘Se isso foi dito pelo Abençoado, senhor,
então assim é.’ Como um pupilo sempre aplaude qualquer coisa que o seu
professor lhe disser, dizendo: ‘Assim é, mestre, assim é!’; da mesma forma,
Mallika, não importa o que o contemplativo Gotama diga, você<span
style="mso-spacerun: yes">  </span>aplaude o contemplativo desta forma: ‘Se
isso foi dito pelo Abençoado, senhor, então assim é.’<span style="mso-spacerun:
yes">  </span>Vá embora, Mallika, saia daqui!”</p>



<p class=Normal>6. Então, a Rainha Mallika se dirigiu ao brâmane Nalijangha:
“Venha, brâmane, vá até o Abençoado e preste uma homenagem em meu nome com a
sua cabeça aos pés dele e pergunte se ele está livre de enfermidades e
aflições, se está com saúde, forte e vivendo com conforto, dizendo:<span
style="mso-spacerun: yes">  </span>‘Venerável senhor, a Rainha Mallika
homenageia o Abençoado com a cabeça aos seus pés e pergunta se o Abençoado está
livre de enfermidades ... e vivendo com conforto.’ Então diga o seguinte:
‘Venerável senhor, estas palavras foram pronunciadas pelo Abençoado: “Tristeza,
lamentação, dor, angústia e desespero nascem daqueles que amamos, surgem
daqueles que amamos”?’ Lembre bem da resposta do Abençoado e venha relatá-la a
mim; pois os Tathagatas não dizem mentiras.”</p>

<p class=Normal>“Sim, senhora,” ele respondeu, e foi até o Abençoado e ambos
se cumprimentaram. Quando a conversa amigável e cortês havia terminado, ele
sentou a um lado e disse: ‘Mestre Gotama, a Rainha Mallika presta uma homenagem
com a cabeça aos pés do Mestre Gotama e pergunta se ele está livre de enfermidades ... e
vivendo com conforto. E ela diz o seguinte: ‘Venerável senhor, estas palavras
foram pronunciadas pelo Abençoado: “Tristeza, lamentação, dor, angústia e
desespero nascem daqueles que amamos, surgem daqueles que amamos”?’”</p>



<p class=Normal>7. “Assim é, brâmane, assim é! Tristeza, lamentação, dor,
angústia e desespero nascem daqueles que amamos, surgem daqueles que amamos.</p>



<p class=Normal>8. “Pode ser compreendido através do seguinte, brâmane, como
a tristeza, lamentação, dor, angústia e desespero nascem daqueles que amamos,
surgem daqueles que amamos. Certa vez, aqui mesmo em Savatthi, havia uma certa
mulher cuja mãe havia morrido. Devido à morte da mãe, ela enlouqueceu, perdeu o
controle da mente e perambulava de rua em rua e de cruzamento em cruzamento,
dizendo: ‘Você viu minha mãe? Você viu minha mãe?’</p>



<p class=Normal>9-14. “E também pode ser compreendido através do seguinte,
como a tristeza, lamentação, dor, angústia e desespero nascem daqueles que
amamos, surgem daqueles que amamos. Certa vez, aqui mesmo em Savatthi, havia
uma certa mulher cujo pai havia morrido ... cujo irmão havia morrido ... cuja
irmã havia morrido ... cujo filho havia morrido ... cuja filha havia morrido ...
cujo marido havia morrido. Devido à morte do marido, ela enlouqueceu, perdeu o
controle da mente e perambulava de rua em rua e de cruzamento em cruzamento,
dizendo: ‘Você viu meu marido? Você viu meu marido?’</p>



<p class=Normal>15-21. “E também pode ser compreendido através do seguinte,
como a tristeza, lamentação, dor, angústia e desespero nascem daqueles que
amamos, surgem daqueles que amamos. Certa vez, aqui mesmo em Savatthi, havia
uma certo homem cuja mãe havia morrido… cujo pai havia morrido ... cujo irmão
havia morrido ... cuja irmã havia morrido ... cujo filho havia morrido ... cuja
filha havia morrido ... cuja esposa havia morrido. Devido à morte da esposa, ele
enlouqueceu, perdeu o controle da mente e perambulava de rua em rua e de
cruzamento em cruzamento, dizendo: ‘Você viu minha esposa? Você viu minha
esposa?’</p>



<p class=Normal>22. “E também pode ser compreendido através do seguinte,
como a tristeza, lamentação, dor, angústia e desespero nascem daqueles que
amamos, surgem daqueles que amamos. Certa vez, aqui mesmo em Savatthi, havia
uma certa mulher que foi viver com os seus pais. Os pais queriam que
ela se separasse do seu marido e que ela se casasse com um outro de quem ela
não gostava. Então a mulher disse para o marido: ‘Senhor, os meus pais
querem que eu me separe de você para que me case com um outro de quem não
gosto.’ Então o homem esquartejou a mulher em dois pedaços e se suicidou,
pensando: ‘Estaremos juntos no além.’ Também pode ser compreendido através
disso, como a tristeza, lamentação, dor, angústia e desespero nascem daqueles
que amamos, surgem daqueles que amamos.”</p>



<p class=Normal>23. Então, satisfeito e contente com as palavras do Abençoado,
o brâmane Nalijangha se levantou do assento e foi até a Rainha Mallika e
relatou toda a conversa com o Abençoado.</p>



<p class=Normal>24. Então a Rainha Mallika foi até o Rei Pasenadi de Kosala
e lhe perguntou: “O que você pensa, senhor? A Princesa Vajiri é amada por
você?”</p>

<p class=Normal>“Sim, Mallika, a Princesa Vajiri á amada por mim.”</p>

<p class=Normal>“O que você pensa, senhor? Se mudanças e alterações [<a
href="#N1">1</a>] <a name=R1></a>ocorressem na Princesa Vajiri, a tristeza,
lamentação, dor, angústia e desespero surgiriam em você?”</p>

<p class=Normal>“Mudanças e alterações na Princesa Vajiri significariam
alterações na minha vida. Como poderiam a tristeza, lamentação, dor, angústia e
desespero não surgirem em mim?”</p>

<p class=Normal>“Foi com referência a isso, senhor, que o Abençoado que sabe
e vê, um arahant, perfeitamente iluminado, disse: ‘Tristeza, lamentação, dor,
angústia e desespero nascem daqueles que amamos, surgem daqueles que amamos.’”</p>



<p class=Normal>25-28. “O que você pensa, senhor? A nobre Rainha Vasabha é
amada por você? ... O General Vidudabha é amado por você? ... Eu sou amada por
você? ... Benares e Kosala são amadas por você?” [<a href="#N2">2</a>]</p>

<p class=Normal><a name=R2></a>“Sim, Mallika, Benares e Kosala são amadas
por mim. É devido a Benares e Kosala que usamos o sândalo de Benares e usamos
grinaldas, perfumes e ungüentos.”</p>

<p class=Normal>“O que você pensa, senhor? Se mudanças e alterações
ocorressem em Benares e Kosala, a tristeza, lamentação, dor, angústia e
desespero surgiriam em você?”</p>

<p class=Normal>“Mudanças e alterações em Benares e Kosala significariam
alterações na minha vida. Como poderiam a tristeza, lamentação, dor, angústia e
desespero não surgirem em mim?”</p>

<p class=Normal>“Foi com referência a isso, senhor, que o Abençoado que sabe
e vê, um arahant, perfeitamente iluminado, disse: ‘Tristeza, lamentação, dor,
angústia e desespero nascem daqueles que amamos, surgem daqueles que amamos.’”</p>



<p class=Normal>29. “É maravilhoso, Mallika, é admirável, quão grande é o
entendimento do Abençoado e como ele vê com sabedoria. Venha, Mallika, me dê a
água para ablução.” [<a href="#N3">3</a>]</p>

<p class=Normal><a name=R3></a>Então o Rei Pasenadi de Kosala levantou do
seu assento e arrumando o manto externo sobre o ombro, juntou as mãos em
respeitosa saudação na direção do Abençoado e pronunciou esta exclamação três
vezes: “Honra ao Abençoado, um arahant, perfeitamente iluminado!<span
style="mso-spacerun: yes">  </span>Honra ao Abençoado, um arahant, perfeitamente
iluminado! Honra ao Abençoado, um arahant, perfeitamente iluminado!”<span
lang=PT-BR style='mso-ansi-language:PT-BR'><o:p></o:p></span></p>


