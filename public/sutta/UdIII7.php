<p class=Normal>Assim ouvi. Em certa ocasião, o Abençoado estava em Rajagaha, no Bambual, no Santuário dos Esquilos. </p>



<p class=Normal>Agora, naquela ocasião o venerável Maha Kassapa estava na Caverna Pipphali, sentado por sete dias em meditação, após ter alcançado certo nível de concentração. Então, ao fim do sétimo dia, ele emergiu de tal concentração. Então, ao emergir de tal concentração, lhe ocorreu o seguinte pensamento: "E se eu fosse até Rajagaha para esmolar alimentos?”</p>



<p class=Normal>Naquela ocasião, uma grande quantidade de devas – uns quinhentos devas – se encontravam ansiosos pela oportunidade de oferecer alimentos para o Venerável Maha Kassapa. Mas o Venerável Maha Kassapa, tendo recusado a oferenda destes quinhentos devas, bem cedo pela manhã ajustou seu manto interno e – carregando a sua tigela e mantos – foi para Rajagaha esmolar alimentos.</p>



<p class=Normal>E naquele momento, Sakka, o senhor dos devas, se encontrava desejoso de fazer oferendas de alimentos para o Venerável Maha Kassapa. Ele então assumiu a forma de um tecelão trabalhando em seu tear, enquanto Sujata, uma jovem asura, preenchia lançadeiras. Conforme o Venerável Maha Kassapa seguia esmolando de casa em casa em Rajagaha, ele chegou à casa onde Sakka se encontrava. </p>



<p class=Normal>Sakka, o senhor dos devas, viu o venerável Maha Kassapa ao longe e, ao vê-lo, saiu para encontrá-lo. Tomando a tigela em sua mão, Sakka entrou em casa, colocou nesta arroz cozido e em seguida devolveu ao venerável Maha Kassapa. Foi feita também a oferenda de muitos tipos de molhos e de caril.</p>



<p class=Normal>Foi então que ocorreu ao venerável Maha Kassapa o seguinte pensamento: "Quem será este ser, com tamanho poder e majestade?" E em seguida, ocorreu ao venerável Maha Kassapa outro pensamento: "Este só pode ser Sakka, o senhor dos devas." Diante disto, ele disse: "Você está por trás disso, Kosiya, você não deve fazer tal coisa novamente.”</p>



<p class=Normal>"Nós também temos necessidade de realizar atos meritórios, venerável Kassapa, nós também temos o dever de realizar méritos.”</p.



<p class=Normal>Então, após homenagear e circundar três vezes o venerável Maha Kassapa, mantendo-o à sua direita, Sakka subiu ao céu e, enquanto no ar, visível no céu, exclamou três vezes:</p>



<p class=Normal>"Ah, oferendas! Excelentes e bem direcionadas são as oferendas feitas ao Venerável Kassapa!”</p>



<p class=Normal>Enquanto isso, com o ouvido divino, que é purificado e ultrapassa o humano, o Abençoado ouviu Sakka enquanto no ar, visível no céu, exclamar três vezes:</p>



<p class=Normal>"Ah, oferendas! Excelentes e bem direcionadas são as oferendas feitas ao venerável Kassapa!”</p>



<p class=Normal>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p>



	<p class=Normal style='margin-left:.75in'>"Vivendo do que é oferecido,<br>

	e assim sustentando a si mesmo,<br>

	sem o fardo de sustentar outrém.<br>

	Assim vive aquele que é invejado pelos devas,<br>

	acalmado, em paz, plenamente atento e consciente.”</p>


