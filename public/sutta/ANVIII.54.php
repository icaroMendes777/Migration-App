<p class=Normal style='margin-left:.75in'>


<p class=Normal>Assim ouvi. Em certa ocasião o Abençoado estava entre os
Koliyas em uma cidade chamada Kakkarapatta. Nessa ocasião Dighajanu </span>foi
até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse:<span
style='mso-ansi-language:PT-BR'> <span lang=PT-BR><o:p></o:p></span></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Nós somos discípulos
leigos que desfrutamos dos prazeres dos sentidos; vivemos numa casa cheia de
crianças; gozamos do sândalo de Benares; usamos grinaldas, perfumes e
ungüentos; recebemos ouro e prata. Que o Abençoado possa ensinar o Dhamma para
pessoas como nós, para a nossa felicidade e bem-estar nesta vida, para nossa
felicidade e bem-estar nas vidas que virão&quot;.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>[O Abençoado disse:]
&quot;Existem essas quatro qualidades, Dighajanu, que trazem a felicidade e bem-estar para um leigo nesta vida. Quais quatro? Ser consumado no esforço
persistente, ser consumado em vigilância, ter pessoas admiráveis como bons
amigos e manter um modo de vida equilibrado. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;E o que significa ser
consumado no esforço persistente? É o caso em que um leigo, qualquer que seja a
ocupação através da qual ele obtenha o seu sustento - pela agricultura ou
comércio ou cuidando do gado ou através do arco ou como um serviçal do rei ou
qualquer outra arte - ele a executa de forma hábil e incansável, possui o discernimento
da sua técnica, ele é capaz de organizar e realizar sua tarefa. A isto se
denomina ser consumado no esforço persistente. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;E o que significa ser
consumado em vigilância? É o caso em que um leigo possui riqueza íntegra -
ganha de forma correta, proveniente da sua própria iniciativa, do seu empenho,
do seu esforço, acumulada pela força do seu braço, obtida com o seu suor - ele
consegue protegê-la através da vigilância [com o pensamento], 'Como farei para
que reis ou ladrões não levem consigo estes meus bens, nem que o fogo os
queime, nem que a água os arraste, nem que herdeiros odiosos a levem consigo?'
A isto se denomina ser consumado em vigilância. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;E o que significa ter
pessoas admiráveis como bons amigos? É o caso em que um leigo, em qualquer cidade
ou vilarejo que ele viva, ele passa o tempo com chefes de família ou filhos de
chefes de família, jovens ou idosos, que possuem a virtude desenvolvida. Ele
conversa com eles, participa de discussões com eles. Ele emula a convicção
consumada daqueles que são consumados em convicção, virtude consumada daqueles
que são consumados em virtude, generosidade consumada daqueles que são
consumados em generosidade e sabedoria consumada daqueles que são consumados em
sabedoria. A isto se denomina ter pessoas admiráveis como bons amigos.<span
style="mso-spacerun: yes">        </span><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;E o que significa
manter um modo de vida equilibrado? É o caso em que um leigo, conhecendo a sua
receita e despesa, mantém um modo de vida equilibrado, nem perdulário nem
mesquinho, [pensando], 'Dessa forma a minha receita irá exceder minha despesa e
a minha despesa não irá exceder a minha receita'. Tal como um pesador ou<a
name=weigher> seu aprendi</a>z, ao segurar a balança, sabe, 'Ela se inclinou
para baixo este tanto ou se inclinou para cima este tanto', da mesma forma, o
leigo, conhecendo a sua receita e despesa, mantém um modo de vida equilibrado,
nem perdulário nem mesquinho, [pensando], ‘Dessa forma a minha receita irá
exceder minha despesa e a minha despesa não irá exceder a minha receita'. Se um
leigo possui uma receita diminuta mas mantém um modo de vida luxuoso, haverá
rumores a seu respeito, ‘Esse membro do clã devora a sua fortuna tal como um
devorador de árvores frutíferas [Nota: alguém que sacode mais frutas de uma
árvore do que possivelmente poderia comer].' Se uma pessoa leiga possui uma
grande receita mas mantém um modo de vida miserável, haverá rumores a seu
respeito, 'Esse membro do clã irá morrer de fome'. Mas quando uma pessoa leiga,
conhecendo a receita e a despesa da sua fortuna, mantém um modo de vida
equilibrado, nem perdulário nem mesquinho, [pensando], 'Dessa forma a minha
receita irá exceder minha despesa e a minha despesa não irá exceder a minha
receita', a isto se denomina manter um modo de vida equilibrado. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Existem quatro
escoadouros da fortuna de uma pessoa: ser seduzida pelos prazeres dos sentidos; ser
seduzida pela bebida; ser seduzida pelo jogo; e ter pessoas más como amigos,
associados e companheiros. Tal como se houvesse um grande reser<a
name=reservoir>vatório co</a>m quatro condutos e quatro escoadouros e um homem
fechasse os condutos e abrisse os escoadouros e o céu não vertesse chuvas
adequadas, o esgotamento daquele grande reservatório poderia ser previsto, não
o seu incremento. Da mesma forma, existem esses quatro escoadouros da fortuna
de uma pessoa: ser seduzida pelos prazeres dos sentidos; ser seduzida pela bebida;
ser seduzida pelo jogo; e ter pessoas más como amigos, associados e
companheiros. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Existem esses quatro
condutos da fortuna de uma pessoa: não ser seduzida pelos prazeres dos sentidos;
não ser seduzida pela bebida; não ser seduzida pelo jogo; e ter pessoas
admiráveis como bons amigos, associados e companheiros. Tal como se houvesse um
grande reservatório com quatro condutos e quatro escoadouros e um homem abrisse
os condutos e fechasse os escoadouros e o céu vertesse chuvas adequadas, o
incremento daquele grande reservatório poderia ser previsto, não o seu
esgotamento. Da mesma forma, existem esses quatro condutos da fortuna de uma
pessoa: não ser seduzida pelos prazeres dos sentidos; não ser seduzida pela bebida;
não ser seduzida pelo jogo; e ter pessoas admiráveis como bons amigos,
associados e companheiros. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Essas, Dighajanu, são
as quatro qualidades que trazem a felicidade e bem-estar para uma pessoa nesta
vida. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Existem essas quatro
qualidades que trazem a felicidade e bem-estar para um leigo nas vidas que
virão. Quais quatro? Ser consumado em convicção, ser consumado em virtude, ser
consumado em generosidade, ser consumado em sabedoria. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;E o que significa ser
consumado em convicção? É o caso em que um nobre discípulo tem convicção, está </span>convencido<span
lang=PT-BR style='mso-ansi-language:PT-BR'> da iluminação do Tathagata: 'De
fato, o </span>Abençoado é um arahant, perfeitamente iluminado, consumado
no verdadeiro conhecimento e conduta, bem-aventurado, conhecedor dos mundos, um
líder insuperável de pessoas preparadas para serem treinadas, mestre de devas
e humanos, desperto, sublime<span lang=PT-BR style='mso-ansi-language:PT-BR'>.’
A isto se denomina ser consumado em convicção. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;E o que significa ser
consumado em virtude? É o caso em que um nobre discípulo se abstém de matar, se
abstém de roubar, se abstém de conduta sexual imprópria, se abstém de mentir,
se abstém de tomar vinho, álcool e outros embriagantes que causam a
negligência. A isto se denomina ser consumado em virtude. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;E o que significa ser
consumado em generosidade? É o caso em que um nobre discípulo </span>permanece
em casa com uma mente desprovida da mácula da avareza, espontaneamente
generoso, mão aberta, que se delicia com a renúncia, devotado à caridade,
deliciando-se em dar e compartir<span lang=PT-BR style='mso-ansi-language:PT-BR'>.
A isto se denomina ser consumado em generosidade. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;E o que significa ser
consumado em </span>sabedoria<span lang=PT-BR style='mso-ansi-language:PT-BR'>?
É o caso em que um nobre discípulo </span>tem sabedoria, com a
completa compreensão da origem e cessação - nobre, penetrante, que conduz ao fim do
sofrimento<span lang=PT-BR style='mso-ansi-language:PT-BR'>. A isto se denomina
ser consumado em sabedoria. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Essas, Dighajanu, são
as quatro qualidades que trazem a felicidade e bem-estar para uma pessoa nas
vidas que virão.&quot; <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:PT-BR'>Diligente
na administração<br>
ou trabalhando na sua ocupação,<br>
mantendo a vida equilibrada,<br>
<span style="mso-spacerun: yes">    </span>a pessoa protege a sua fortuna.<br>
Uma pessoa com convicção,<br>
perfeita em virtude,<br>
magnânima, livre de egoísmo,<br>
&nbsp;&nbsp;&nbsp; constantemente limpa o caminho<br>
&nbsp;&nbsp;&nbsp; para proteção das vidas que virão.<br>
Dessa forma para aquele que busca a vida em família,<br>
essas oito qualidades, que trazem o bem-estar e a felicidade<br>
tanto nesta vida como nas vidas que virão,<br>
foram declaradas por aquele<br>
&nbsp;&nbsp;&nbsp; cujo nome<br>
&nbsp;&nbsp;&nbsp; é a verdade.<br>
E é assim como, para aqueles que vivem em família,<br>
a generosidade e o mérito se incrementam. <o:p></o:p></span></p>
