<p class=Normal> Em Savatthi. "Aqui, bhikkhus, a pessoa comum sem instrução considera a forma como o eu; ou o eu como possuído de forma material; ou a forma material como estando no eu; ou o eu como estando na forma material. Isso é chamado, bhikkhus, uma pessoa comum sem instrução que está atada pela sujeição à forma, que está atada por uma sujeição interior e exterior, que não vê a margem mais próxima nem a margem mais distante, que envelhece atada, que morre atada, atada irá deste mundo para um outro mundo. </p>

<p class=Normal>"Ela considera a sensação como o eu ... a percepção como o eu ... as formações volitivas como o eu ... a consciência como o eu, ou o eu como possuído de consciência; ou a consciência como estando no eu; ou o eu como estando na consciência. Isso é chamado, bhikkhus, uma pessoa comum sem instrução que está atada pela sujeição à consciência, que está atada por uma sujeição interior e exterior, que não vê a margem mais próxima nem a margem mais distante, que envelhece atada, que morre atada, atada irá deste mundo para um outro mundo. </p>

<p class=Normal>"Mas, bhikkhus, o nobre discípulo bem instruído não considera a forma como o eu; ou o eu como possuído de forma material; ou a forma material como estando no eu; ou o eu como estando na forma material. Isso é chamado, bhikkhus, um nobre discípulo bem instruído que não está atado pela sujeição à forma, que não está atado por uma sujeição interior e exterior, que vê a margem mais próxima e a margem mais distante. Que está livre do sofrimento, eu digo. </p>

<p class=Normal>"Ele não considera a sensação como o eu ... a percepção como o eu ... as formações volitivas como o eu ... a consciência como o eu, ou o eu como possuído de consciência; ou a consciência como estando no eu; ou o eu como estando na consciência. Isso é chamado, bhikkhus, um nobre discípulo bem instruído que não está atado pela sujeição à consciência, que não está atado por uma sujeição interior e exterior, que vê a margem mais próxima e a margem mais distante. Que está livre do sofrimento, eu digo." </p>


