<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em<a
name=savatthi> Savatthi</a>. &quot;Bhikkhus, existem esses quatro tipos de
alimentos para a manutenção dos seres que já nasceram e para o sustento
daqueles que estão em busca de um nascimento. Quais quatro? O alimento comida,
grosseira ou sutil, o contato como o segundo, a volição mental como o terceiro
e a consciência como o quarto. Esses são os quatro tipos de alimento para a
manutenção dos seres que já nasceram e para o sustento daqueles que estão
buscando o nascimento. <o:p></o:p></span></p>

<p class=Normal> &quot;E
como deve ser encarado o alimento comida? Suponham que um casal, marido e
esposa, carregando consigo parcas provisões, saíssem em viagem pelo deserto.
Com eles estaria o seu único filho, um bebê, querido e encantador. Então, as
parcas provisões do casal, nessa viagem, seriam consumidas e esgotadas e ainda
haveria uma extensão do deserto por ser atravessada. Este pensamento lhes
ocorreria, 'As nossas parcas provisões foram consumidas e estão esgotadas e
ainda falta uma extensão do deserto por atravessar. E se nós matássemos esse
nosso filho, bebê, querido e encantador, e secássemos a sua carne. Assim -
mastigando a carne do nosso filho - pelo menos nós dois sobreviveríamos no
deserto. De outra forma, nós três pereceríamos'. Assim eles mataram o seu único
filho, bebê, querido e encantador, e secaram a sua carne. Mastigando a carne do
seu filho, eles conseguiram atravessar o deserto. Enquanto comiam a carne do
seu único filho, eles batiam no peito, (chorando), 'Para onde você foi, nosso
único filho? 'Para onde você foi, nosso único filho?' Agora o que vocês
pensam, bhikkhus: Aquele casal comeria aquela comida por prazer ou por diversão
ou para ganhar peso ou para se embelezar?&quot; <o:p></o:p></span></p>

<p class=Normal> &quot;Não,
senhor.&quot; <o:p></o:p></span></p>

<p class=Normal> &quot;Eles
não comeriam aquela comida simplesmente para conseguir atravessar o
deserto?&quot; <o:p></o:p></span></p>

<p class=Normal> &quot;Sim,
senhor.&quot; <o:p></o:p></span></p>

<p class=Normal> &quot;Da
mesma forma, eu lhes digo, o alimento comida deve ser considerado. Quando o
alimento comida é compreendido, a cobiça pelos cinco elementos do prazer sensual
é compreendida . Quando a cobiça pelos cinco elementos do prazer sensual é
compreendida, não existe grilhão pelo qual um nobre discípulo retornaria
novamente a este mundo. <o:p></o:p></span></p>

<p class=Normal> &quot;E
como deve ser encarado o alimento contato? Suponham que uma vaca esfolada e<a
name=cow>stivess</a>e apoiada contra uma parede. As criaturas que vivem na
parede a mordiscariam. Se ela se apoiasse contra uma árvore, as criaturas que
vivem na árvore a mordiscariam. Se ela ficasse exposta à água, as criaturas que
vivem na água a mordiscariam. Se ela ficasse exposta ao ar, as criaturas que
vivem no ar a mordiscariam. Pois em qualquer lugar que a vaca ficasse exposta,
as criaturas que ali vivem a mordiscariam. Do mesmo modo, eu lhes digo, deve
ser encarado o alimento contato. Quando o alimento contato é compreendido, os
três tipos de sensação (prazer, dor e neutra) são compreendidas. Quando os três
tipos de sensação são compreendidas, eu lhes digo, não existe nada mais a ser
feito por um nobre discípulo. <o:p></o:p></span></p>

<p class=Normal> &quot;E
como deve ser encarado o alimento volição mental? Suponham que<a name=pit>
houves</a>se uma cova com carvão em brasa, mais profunda que a altura de um
homem, repleta de carvão em brasas sem arder e fumegar, e surgisse um homem </span>que
valoriza a vida e teme a morte, que deseja o prazer e abomina a dor,  e dois
homens fortes, agarrando-o pelos braços, o arrastassem até a cova cheia de
carvão em brasas. A intenção do homem seria distanciar-se, o desejo dele seria
distanciar-se, a aspiração dele seria distanciar-se. Por que isso? Porque ele
compreenderia, 'Se eu cair nessa cova cheia de carvão em brasas, isso fará com
que eu morra, ou que eu sinta dores mortíferas'. Do mesmo modo, eu lhes digo,
deve ser encarado o alimento volição mental. Quando o alimento volição mental é
compreendido, as três formas de desejo (pela sensualidade, por ser/existir e
por não ser/existir) são compreendidas. Quando as três formas do desejo são
compreendidas, eu lhes digo, não existe nada mais a ser feito por um nobre
discípulo. <o:p></o:p></span></p>

<p class=Normal> &quot;E
como deve ser encarado o alimento consciência? Suponham que tendo pr<a
name=thief>endido </a>um ladrão, um criminoso, eles o mostrassem ao rei: ‘Este
é um ladrão, um criminoso, vossa majestade. Imponha-lhe a pena que desejar’. </span><span
lang=PT-BR style='mso-ansi-language:PT-BR'>Então o rei diria: ‘Vão
homens, e, pela manhã, atirem nele cem lanças.’ Assim, pela manhã, eles
atirariam nele cem lanças. Ao meio dia o rei diria, ‘Homens, como está aquele
prisioneiro?’ – ‘Ele ainda está vivo majestade.’ Então o rei diria, ‘Vão
homens, e atirem nele mais cem lanças.’ Assim, eles atirariam nele mais cem
lanças, ao meio dia. Ao anoitecer o rei diria, ‘Homens, como está aquele
prisioneiro?’ – ‘Ele ainda está vivo majestade.’ Então o rei diria, ‘Vão
homens, e atirem nele mais cem lanças, ao anoitecer.’ Assim, ao anoitecer, eles
atirariam nele mais cem lanças. Agora o que vocês pensam, bhikkhus: aquele
homem, tendo sido atingido por trezentas lanças ao longo do dia, experimentaria
dor e desprazer devido a isso?”<span style="mso-spacerun: yes">  </span><o:p></o:p></span></p>

<p class=Normal> &quot;Mesmo
se ele fosse atingido por apenas uma lança, senhor, ele experimentaria dor e
desprazer devido a isso, o que não dizer de trezentas lanças.&quot; <o:p></o:p></span></p>

<p class=Normal> &quot;Do
mesmo modo`, eu lhes digo, bhikkhus, deve ser encarado o alimento consciência.
Quando o alimento consciência é compreendido, a mentalidade-materialidade (nome e forma) é
compreendida. Quando a mentalidade-materialidade (nome e forma) é compreendida, eu lhes digo,
não existe nada mais a ser feito por um nobre discípulo.&quot; <o:p></o:p></span></p>


