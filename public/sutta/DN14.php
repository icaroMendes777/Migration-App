cessação,’ surgiu a visão, surgiu o conhecimento, surgiu a sabedoria,<span
style='mso-spacerun:yes'>  </span>surgiu o conhecimento verdadeiro, surgiu a
iluminação em relação a coisas nunca antes ouvidas. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.22.
