<p class=Normal> "Bhikkhus, no passado houve uma batalha entre os devas e os asuras. Nessa batalha os asuras foram vitoriosos e os devas foram derrotados. Derrotados, os devas fugiram para o norte, perseguidos pelos asuras. Então os devas pensaram: 'Os asuras ainda estão nos perseguindo. Lutemos contra eles uma segunda vez.' Uma segunda vez os devas batalharam contra os asuras, e uma segunda vez os asuras foram vitoriosos e os devas foram derrotados. Derrotados, os devas fugiram para o norte, perseguidos pelos asuras. Então os devas pensaram: 'Os asuras ainda estão nos perseguindo. Lutemos contra eles uma terceira vez.' Uma terceira vez os devas batalharam contra os asuras, e uma terceira vez os asuras foram vitoriosos e os devas foram derrotados. Derrotados e amedrontados os devas foram para a sua cidade. </p>

