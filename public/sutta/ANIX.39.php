<p class=Normal> "Bhikkhus, no passado houve uma batalha entre os devas e os asuras. Nessa batalha os devas foram vitoriosos e os asuras foram derrotados. Derrotados, os asuras fugiram para o norte, perseguidos pelos devas. Então os asuras pensaram: 'Os devas ainda estão nos perseguindo. Lutemos contra eles uma segunda vez.' Uma segunda vez os asuras batalharam contra os devas, e uma segunda vez os devas foram vitoriosos e os asuras foram derrotados. Derrotados, os asuras fugiram para o norte, perseguidos pelos devas. Então os asuras pensaram: 'Os devas ainda estão nos perseguindo. Lutemos contra eles uma terceira vez.' Uma terceira vez os asuras batalharam contra os devas, e uma terceira vez os devas foram vitoriosos e os asuras foram derrotados. Derrotados e amedrontados os asuras foram para a sua cidade. </p>

<p class=Normal> "Depois de terem entrado na sua cidade, os asuras pensaram: 'Agora estamos a salvo do perigo e os devas não podem fazer nada contra nós.' Os devas também pensaram: 'Agora os asuras estão a salvo do perigo e não podemos fazer nada contra eles.'

<p class=Normal> (1) Da mesma forma, bhikkhus, quando afastado dos prazeres sensuais, afastado das qualidades não hábeis, um bhikkhu entra e permanece no primeiro jhana ... nessa ocasião o bhikkhu pensa: 'Agora estou a salvo do perigo e Mara não pode fazer nada contra mim.' Mara também pensa: 'Agora o bhikkhus está a salvo do perigo e não posso fazer nada contra ele.'</p>

<p class=Normal> (2) - (4) "Quando, abandonando o pensamento aplicado e sustentado, um bhikkhu entra e permanece no segundo jhana ... no terceiro jhana ... no quarto jhana ... nessa ocasião o bhikkhu pensa: 'Agora estou a salvo do perigo e Mara não pode fazer nada contra mim.' Mara também pensa: 'Agora o bhikkhus está a salvo do perigo e não posso fazer nada contra ele.'</p>

<p class=Normal> (5) "Quando, com a completa superação das percepções da forma, com o desaparecimento das percepções do contato sensorial, sem dar atenção às percepções da diversidade, consciente de que o ‘espaço é infinito,’ ele entra e permanece na base do espaço infinito, nessa ocasião ele é chamado um bhikkhu que cegou Mara, deu um fim aos olhos de Mara sem deixar vestígios, foi além da visão de Mara. </p>

<p class=Normal> (6) - (9) "Quando, com a completa superação da base do espaço infinito um bhikkhu entra e permanece na base da consciência infinita ... com a completa superação da base da consciência infinita, um bhikkhu entra e permanece na base do nada ... com a completa superação da base do nada, um bhikkhu entra e permanece na base da nem percepção, nem não percepção ... com a completa superação da base da nem percepção, nem não percepção, um bhikkhu entra e permanece na cessação da percepção e sensação, e tendo visto com sabedoria, as suas impurezas são destruídas, nessa ocasião ele é chamado um bhikkhu que cegou Mara, deu um fim aos olhos de Mara sem deixar vestígios, foi além da visão de Mara, tendo superado o apego ao mundo."</p>

