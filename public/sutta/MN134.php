<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião, o Abençoado estava em Savatthi no Bosque de Jeta, no
Parque de Anathapindika. Agora, naquela ocasião o venerável Lomasakangiya
estava no país dos Sakyas em Kapilavatthu no Parque de Nigrodha. [<a href="#N1">1</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2. E assim, quando a noite estava bem avançada, Candana, um jovem deva
com belíssima aparência que iluminou toda a área do Parque de Nigrodha, se
aproximou do venerável Lomasakangiya. Ficando em pé a um lado, Candana disse:<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhu,
você se lembra do sumário e da análise ‘Daquele que teve Uma Única Noite
Excelente’?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigo, eu
não me lembro do sumário e da análise ‘Daquele que teve Uma Única Noite
Excelente.’ Mas, amigo, você se lembra do sumário e da análise ‘Daquele que
teve Uma Única Noite Excelente’?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhu,
eu também não me lembro do sumário e da análise ‘Daquele que teve Uma Única
Noite Excelente.’ Mas, bhikkhu, você se lembra dos versos ‘Daquele que teve Uma
Única Noite Excelente’?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigo, eu
não me lembro dos versos ‘Daquele que teve Uma Única Noite Excelente.’ Mas,
amigo, você se lembra dos versos ‘Daquele que teve Uma Única Noite Excelente’?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhu,
eu me lembro dos versos ‘Daquele que teve Uma Única Noite Excelente.’ <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas,
amigo, como você se lembra dos versos ‘Daquele que teve Uma Única Noite
Excelente.’?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhu,
certa vez o Abençoado estava entre os devas do paraíso dos Trinta e
Três, na pedra de Mármore Vermelha, ao pé da árvore Paricchattaka. [<a
href="#N2">2</a>] <a name=R2></a>Lá, o Abençoado recitou o sumário e a análise
‘Daquele que teve Uma Única Noite Excelente’ para os devas do paraíso do
Trinta e Três:<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. ‘Que
ninguém reviva o passado <br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>ou no
futuro coloque as suas esperanças; <br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>pois o
passado foi deixado para trás<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>e o futuro
ainda não foi alcançado.<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>Ao invés disso,
que ele veja com insight<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>cada estado
surgido no presente;<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>que ele
compreenda isso e tenha certeza disso,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>invencível,
inabalável.<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>Hoje o
esforço tem que ser feito;<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>amanhã a
Morte poderá vir, quem sabe?<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>Não há
barganha com a Mortalidade<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>que a
mantenha, com a sua horda, fora disso,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>mas aquele
que permanece assim ardente,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>decidido,
durante o dia, durante a noite - <br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>é ele,
disse o Sábio em Paz; <br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>que teve
uma única noite excelente.’ <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4.
“Bhikkhu, eu me lembro dos versos ‘Daquele que teve Uma Única Noite Excelente.’
Bhikkhu, aprenda o sumário e a análise ‘Daquele que teve Uma Única Noite
Excelente.’ Bhikkhu, obtenha proficiência no sumário e na análise ‘Daquele
que teve Uma Única Noite Excelente.’ Bhikkhu, lembre-se do sumário e da análise ‘Daquele que teve Uma Única Noite Excelente.’ Bhikkhu, o sumário e a análise ‘Daquele que teve Uma Única Noite Excelente’ traz benefício, faz
parte dos fundamentos da vida santa.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o
que Candana disse e em seguida desapareceu de vez.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. Então,
ao amanhecer, o venerável Lomasakangiya arrumou o seu local de descanso e
tomando a tigela e o manto externo foi caminhando em etapas até Savatthi. Ele
por fim chegou em Savatthi e foi até o Abençoado no Bosque de Jeta, no Parque de
Anathapindika. Depois de cumprimentá-lo, ele sentou a um lado e relatou ao
Abençoado tudo que havia ocorrido, e disse: “Seria bom, venerável senhor, se o
Abençoado me ensinasse<span style="mso-spacerun: yes">  </span>o sumário e a análise ‘Daquele que teve Uma Única Noite Excelente.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6.
“Bhikkhu, você conhece aquele jovem deva?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
venerável senhor.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhu, o
nome daquele jovem deva é Candana. Ele tem consideração pelo Dhamma, dá
atenção, ocupa toda a sua mente com isso, ouve com os ouvidos atentos.
Portanto, bhikkhu, ouça e preste muita atenção àquilo que eu vou dizer.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
venerável senhor,” o venerável Lomasakangiya respondeu. O Abençoado disse o
seguinte<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:PT-BR'>7-14. “Que
ninguém reviva o passado...<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>(Repetir
todo o <a href="MN131.php#T3">MN 131.3-10</a> até:)<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>que teve
uma única noite excelente.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o que
disse o Abençoado. O venerável Lomasakangiya ficou satisfeito e contente com as
palavras do Abençoado<o:p></o:p></span></p>


