<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim ouvi.
Em certa ocasião, o Abençoado estava em Rajagaha, no Bambual, no Santuário dos
Esquilos. Agora, naquela ocasião o Venerável Lakkhana e o Venerável
Mahamoggallana estavam na Montanha Pico do Abutre. Então, ao amanhecer, o
Venerável Mahamoggallana se vestiu e tomando a tigela e o manto se aproximou do
Venerável Lakkhana e disse:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venha,
amigo Lakkhana, vamos até Rajagaha esmolar alimentos.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Está bem,
amigo,” o Venerável Lakkhana respondeu. Então enquanto descia da Montanha Pico
do Abutre, o Venerável Mahamoggallana sorriu num certo lugar. O Venerável
Lakkhana disse: “Por que razão, amigo Moggallana, você sorriu?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Este não é
o momento para esse tipo de pergunta, amigo Lakkhana. Pergunte-me isso quando
estivermos na presença do Abençoado.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então,
depois que o Venerável Lakkhana e o Venerável Mahamoggallana haviam esmolado
alimentos em Rajagaha e de haverem retornado, depois da refeição, eles foram até
o Abençoado. Depois de cumprimentar o Abençoado, eles sentaram a um lado e o
Venerável Lakkhana disse para o Venerável Mahamoggallana: “Enquanto descia da
montanha Pico do Abutre, o Venerável Mahamoggallana sorriu num certo lugar. Por
que razão, amigo Moggallana, você sorriu?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigo,
enquanto descia da Montanha Pico do Abutre, eu vi um esqueleto voando pelo ar.
Abutres, corvos e falcões o perseguiam de perto, beliscavam entre as suas costelas,
apunhalavam-no e rasgavam-no em pedaços, enquanto ele urrava de dor. Eu pensei:
‘De fato é espantoso! De fato é impressionante! Que esse tipo de ser possa
existir, que possa existir um espírito assim, que possa haver esse tipo de
existência!’”[<a href="#N1">1</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Então o Abençoado se dirigiu aos bhikkhus da seguinte forma: “Bhikkhus,
há discípulos que realizaram a visão, realizaram o conhecimento, de modo que
esses discípulos são capazes de saber e ver e testemunhar esse tipo de vista.
No passado, bhikkhus, eu também vi esse ser, mas não falei a respeito disso.
Pois se eu tivesse falado a respeito disso, outros poderiam não ter acreditado
em mim, e se eles não acreditassem em mim isso resultaria em dano e sofrimento
para eles por muito tempo. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Esse ser,
bhikkhus, era um açougueiro aqui mesmo em Rajagaha. Tendo sido atormentado no
inferno por muitos anos, muitas centenas de anos, muitos milhares de anos,
muitas centenas de milhares de anos, como resultado desse kamma e como resíduo
desse kamma, ele agora está experimentando esse tipo de existência.”<o:p></o:p></span></p>


