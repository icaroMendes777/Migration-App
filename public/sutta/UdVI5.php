<p class=Normal> Assim ouvi. Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no Parque de Anathapindika. Agora naquela ocasião havia em Savatthi vários brâmanes, contemplativos e errantes de várias seitas com distintas idéias, distintas opiniões, distintas crenças e eles dependiam das suas distintas idéias para o seu sustento. Alguns contemplativos e brâmanes tinham este entendimento, esta doutrina: “O eu e o mundo são eternos. Somente isso é verdadeiro; todo o restante é falso.”</p>

<p class=Normal> Alguns contemplativos e brâmanes tinham este entendimento, esta doutrina: “O eu e o mundo não são eternos” ... “O eu e o mundo são ambos eternos e não eternos”  ... “O eu e o mundo não são nem eternos e tampouco não eternos” ... “O eu e o mundo são produzidos por si mesmo” ... “O eu e o mundo são produzidos pelos outros” ... “O eu e o mundo são ambos produzidos por si mesmo e pelos outros” ... “O eu e o mundo não são nem produzidos por si mesmo e tampouco pelos outros, mas surgem sem uma causa”. </p>

<p class=Normal>  “O prazer e a dor, o eu e o mundo, são produzidos por si mesmo, ... pelos outros ... ambos produzidos por si mesmo e pelos outros ... não são nem produzidos por si mesmo e tampouco pelos outros, mas surgem sem uma causa. Somente isso é verdadeiro; todo o restante é falso.”</p>

<p class=Normal> E eles estavam assim envolvidos em rixas e brigas, mergulhados em discussões, apunhalando uns aos outros usando as palavras como adagas, dizendo, “O Dhamma é assim, não assado. O Dhamma não é assado, é assim.”</p>

<p class=Normal> Então, ao amanhecer, um grande número de bhikkhus se vestiram e tomando a tigela e o manto externo, foram para Savatthi para esmolar alimentos. Depois de haver esmolado em Savatthi e de haver retornado, após a refeição eles foram até o Abençoado e depois de cumprimentá-lo sentaram a um lado e disseram: “Venerável senhor, há em Savatthi vários brâmanes, contemplativos e errantes de várias seitas com distintas idéias, distintas opiniões, distintas crenças e eles dependem das suas distintas idéias para o seu sustento ... e eles estão envolvidos em rixas e brigas, mergulhados em discussões, apunhalando uns aos outros usando as palavras como adagas, dizendo, ‘O Dhamma é assim, não assado. O Dhamma não é assado, é assim.’”</p>

<p class=Normal> “Bhikkhus, os errantes de outras seitas são cegos e sem visão. Eles não compreendem aquilo que é benéfico e aquilo que é prejudicial. Eles não entendem o que é o Dhamma e o que é o não-Dhamma. Sem compreender aquilo que é benéfico e aquilo que é prejudicial, sem entender o que é o Dhamma e o que é o não-Dhamma, eles permanecem envolvidos em rixas e brigas, mergulhados em discussões, apunhalando uns aos outros usando as palavras como adagas, dizendo, ‘O Dhamma é assim, não assado. O Dhamma não é assado, é assim.’”</p>

<p class=Normal>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p>

<p class=Normal style='margin-left:.75in'> Alguns assim chamados contemplativos e brâmanes,<br>
têm profundo apego às suas idéias;<br>
sem encontrar um apoio firme<br>
eles afundam no meio da torrente.</p>

