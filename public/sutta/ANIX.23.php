<p class=Normal style='margin-left:.75in'> 
<p class=Normal><b>Notas:</b></p>

<p class=Normal> O desejo leva à <i>busca</i> dos objetos desejados e no final por meio da busca eles são obtidos. Quando os objetos são obtidos a pessoa toma <i>decisões</i> com relação a eles: o que é meu e o que é seu, o que possui valor e o que não possui, o que irei guardar e o que irei desfrutar. Devido a essas decisões, pensamentos de <i>desejo e cobiça</i> surgem. A pessoa desenvolve <i>apego</i> pelos objetos, adota uma atitude possessiva com relação a eles e acaba se tornando <i>mesquinha</i>, recusando comparti-los com outros. Encarando os outros com medo e suspeita, a pessoa busca como <i>salvaguardar</i> os seus objetos. Quando esse tipo de cobiça e medo se difunde amplamente é necessário apenas uma mínima provocação para irromper em violência, conflitos, etc.<br>
