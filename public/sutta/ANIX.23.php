<p class=Normal> "Bhikkhus, eu lhes ensinarei nove coisas condicionadas pelo desejo. Ouçam e prestem muita atenção àquilo que eu vou dizer.” – “Sim, venerável senhor,” os bhikkhus responderam. O Abençoado disse o seguinte:</p>

<p class=Normal> "Quais são as nove coisas condicionadas pelo desejo? (1) Na dependência do desejo surge a busca; (2) na dependência da busca surge o ganho; (3) na dependência do ganho surge a decisão; (4) na dependência da decisão surge o desejo e a cobiça; (5) na dependência do desejo e da cobiça surge o apego; (6) na dependência do apego surge a posse; (7) na dependência da posse surge a mesquinharia; (8) na dependência da mesquinharia surge a salvaguarda; (9) e devido à salvaguarda, muitos fenômenos ruins e prejudiciais surgem – tomar clavas e armas, conflitos, brigas e disputas, linguagem ofensiva, difamação e falsidades.</p>

