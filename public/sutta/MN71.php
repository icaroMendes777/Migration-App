<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião o Abençoado estava em Vesali na Grande Floresta no Salão
com um Pico na Cumeeira.<o:p></o:p></span></p>



<p class=Normal><a name=T2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2. Agora naquela ocasião o errante Vacchagotta estava no Parque dos
Errantes com a Mangueira com uma Única Flor de Lótus. [<a href="#N1">1</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>3. Então, ao amanhecer, o Abençoado se vestiu e tomando a tigela e o
manto externo foi para Vesali para esmolar alimentos. Então o Abençoado pensou:
“Ainda é muito cedo para esmolar alimentos em Vesali. E se eu fosse até o
errante Vacchagotta no Parque dos Errantes com a Mangueira com Uma Única Flor
de Lótus.<span class=GramE>”</span><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. Então, o
Abençoado foi até o errante Vaccagotta<span class=GramE><span
style='mso-spacerun:yes'>  </span></span>no Parque dos Errantes com a Mangueira
com Uma Única Flor de Lótus. O errante Vaccagotta viu o Abençoado vindo à
distância e lhe disse: “Venha Abençoado, venerável senhor! Bem vindo Abençoado!
Já faz muito tempo desde que o<span class=GramE><span
style='mso-spacerun:yes'>  </span></span>Abençoado encontrou uma oportunidade
para vir aqui. Que o<span style='mso-spacerun:yes'>  </span>Abençoado sente;
este assento está preparado.” O Abençoado sentou no assento que havia sido
preparado. O errante Vaccagotta tomou um assento mais baixo, sentou a um lado e
perguntou ao Abençoado:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5.
Venerável senhor, eu ouvi isto: ‘O contemplativo Gotama declara ser onisciente
e capaz de ver tudo, reivindica ter conhecimento completo e visão desta forma:
“Quer eu esteja andando ou em pé ou dormindo ou acordado, o conhecimento e
visão estão presentes em mim de forma contínua e ininterrupta.” [<a href="#N2">2</a>]
<a name=R2></a>Venerável senhor, aqueles que assim dizem, <span class=GramE>falam</span>
aquilo que foi dito pelo Abençoado e não o deturpam com algo contrário aos
fatos? Eles explicam de acordo com o Dhamma de tal modo que nada que possa dar
margem à censura possa de forma legítima ser deduzido da declaração deles?<span
class=GramE>”</span><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Vaccha, aqueles
que dizem isso não falam aquilo que foi dito por mim, mas me deturpam com algo
que não é verdadeiro e é contrário aos fatos.” [<a href="#N3">3</a><span
class=GramE>]</span><o:p></o:p></span></p>



<p class=Normal><a name=R3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>6. “Venerável senhor, como deverei responder de forma a falar aquilo que
foi dito pelo Abençoado e não deturpá-lo com algo contrário aos fatos? Como
poderei explicar de acordo com o Dhamma de tal modo que nada que possa dar
margem à censura possa de forma legítima ser deduzido da minha declaração?<span
class=GramE>”</span><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Vaccha, se
você responder assim: ‘O contemplativo Gotama possui os três conhecimentos <span
class=GramE>verdadeiros,</span>’ você estará falando o que foi dito por mim e
não irá me deturpar com algo contrário aos fatos. Você irá explicar de acordo
com o Dhamma de tal forma que nada que possa dar margem à censura possa de forma
legítima ser deduzido da sua declaração.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. “Pois
até onde eu queira, eu me recordo das minhas muitas vidas passadas, isto é, um
nascimento, dois <span class=GramE>nascimentos ... </span>(igual ao <a
href="MN51.php#T24">MN 51, verso 24</a>) ... Assim eu me recordo das minhas
muitas vidas passadas nos seus modos e detalhes.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. “E até
onde eu queira, com o olho divino que é purificado e sobrepuja o humano, eu
vejo seres falecendo e renascendo, inferiores e superiores, bonitos e feios,
afortunados e desafortunados. Eu compreendo como os seres prosseguem de acordo
com as suas <span class=GramE>ações ... </span>(igual ao <a href="MN51.php#T25">MN
51, verso 25</a>).<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. <span
class=GramE>“E realizando por mim mesmo com o conhecimento direto, eu aqui e
agora entro e permaneço na libertação da mente e libertação através da sabedoria que
são imaculadas com a destruição de todas as impurezas.</span><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. <span
class=GramE>“ </span>Se você responder assim: ‘O contemplativo Gotama possui os
três conhecimentos verdadeiros,’ você estará falando o que foi dito por mim e
não irá me deturpar com algo contrário aos fatos. Você irá explicar de acordo
com o Dhamma de tal modo que nada que possa dar margem à censura possa de forma
legítima ser deduzido da sua declaração.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. Quando
isso foi dito, o errante Vaccagotta perguntou ao Abençoado: “Mestre Gotama,
existe algum chefe de família que, sem ter abandonado o grilhão da vida em
família, na dissolução do corpo tenha dado um fim ao sofrimento?” [<a href="#N4">4</a><span
class=GramE>]</span><o:p></o:p></span></p>



<p class=Normal><a name=R4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Vaccha, não existe chefe de família que, sem ter abandonado o grilhão
da vida em família, na dissolução do corpo tenha dado um fim ao sofrimento.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. “Mestre
Gotama, existe algum chefe de família que, sem ter abandonado o grilhão da vida
em família, na dissolução do corpo tenha alcançado o paraíso?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Vaccha<span
class=GramE>, existem</span> não somente cem ou duzentos ou trezentos ou
quatrocentos ou quinhentos, mas muitos mais chefes de família que, sem ter
abandonado o grilhão da vida em família, na dissolução do corpo tenham
alcançado o paraíso.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. “Mestre
Gotama, existe algum Ajivaka que na dissolução do corpo tenha dado um fim ao
sofrimento?” [<a href="#N5">5</a><span class=GramE>]</span><o:p></o:p></span></p>



<p class=Normal><a name=R5></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Vaccha, não existe Ajivaka que na dissolução do corpo tenha dado um fim
ao sofrimento.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14. “Mestre
Gotama, existe algum Ajivaka que na dissolução do corpo tenha alcançado o
paraíso?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando me
recordo dos noventa e um ciclos cósmicos passados, Vaccha, não me lembro de
nenhum Ajivaka que, na dissolução do corpo, tenha alcançado o paraíso, com uma
exceção, e ele acreditava na doutrina da eficácia moral da ação, a doutrina da
eficácia moral dos atos.” [<a href="#N6">6</a><span class=GramE>]</span><o:p></o:p></span></p>



<p class=Normal><a name=R6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>15. “Em sendo assim, Mestre Gotama, as outras seitas de errantes estão
vazias até mesmo da possibilidade de alcançar o paraíso.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Em sendo assim,
Vaccha, as outras seitas de errantes estão vazias até mesmo da possibilidade de
alcançar o paraíso.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Isso foi o que disse o Abençoado. O errante Vacchagotta ficou satisfeito
e contente com as palavras do Abençoado. <o:p></o:p></span></p>

