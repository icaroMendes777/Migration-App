<p class=Normal><a name=N3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[3] MA explica que apesar de uma parte da asserção ser válida, o Buda
rejeita toda ela devido à porção que é inválida. A parte da asserção que é
válida é a afirmação que o Buda é onisciente e tudo vê; a parte excessiva é a
afirmação que o conhecimento e visão estão presentes de forma contínua nele. De
acordo com a tradição Theravada o Buda é onisciente no sentido de que ele tem
acesso a todas as coisas que podem ser conhecidas. Ele não pode, no entanto,
conhecer tudo de forma simultânea e precisa dar atenção para aquilo que ele
queira conhecer. No <a href="MN90.php#T8">MN 90.8</a> o Buda diz que é possível
conhecer e ver tudo, embora não de forma simultânea e no <a href="ANIV.24.php">AN
<span class=GramE>IV.</span>24</a> ele afirma que “tudo aquilo que no mundo,
incluindo os seus devas, maras e brahmas, esta população com seus contemplativos
e brâmanes, seus príncipes e o povo é<span style='mso-spacerun:yes'> 
</span>visto, ouvido, sentido, conscientizado, buscado, procurado, ponderado
pela mente: tudo isso eu entendo,” o que para a tradição Theravada significa
uma asserção de onisciência com as limitações mencionadas. Veja também o Miln
102-7. [<a href="#R3">Retorna</a><span class=GramE>]</span><o:p></o:p></span></p>



<p class=Normal><a name=N4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[4] MA explica “o grilhão da vida em família” <i>(gihisamyojana)</i>
como o apego às aquisições de um chefe de família, que MT detalha como sendo
terras, ornamentos, riqueza, cereais, etc. MA diz que embora os textos
mencionem alguns indivíduos que alcançaram o estado de arahant como discípulos
leigos, através do caminho de arahant eles destruíram todo apego a coisas
mundanas e assim adotaram a vida santa como bhikkhus ou morreram em seguida a
essa realização. A questão de arahants leigos é discutida no Mlin 264. [<a
