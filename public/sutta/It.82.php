<p class=Normal> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: "Bhikkhus, entre os devas, três expressões jubilosas são proclamadas de tempos em tempos em certas ocasiões. Quais três? </p>

<p class=Normal>"Na ocasião em que um jovem raspa o cabelo e a barba, veste o manto de cor ocre e decide deixar a vida em família e seguir a vida santa, nessa ocasião entre os devas esta expressão jubilosa é proclamada: 'Um nobre discípulo intenciona batalhar com Mara.' Essa é a primeira expressão jubilosa proclamada pelos devas de tempos em tempos nessa ocasião. </p>

<p class=Normal>"Novamente, bhikkhus, na ocasião em que um nobre discípulo permanece dedicado ao desenvolvimento dos sete fatores da iluminação, nessa ocasião entre os devas esta expressão jubilosa é proclamada: 'Um nobre discípulo está batalhando com Mara.' Essa é a segunda expressão jubilosa proclamada pelos devas de tempos em tempos nessa ocasião. </p>

<p class=Normal>"Novamente, bhikkhus, na ocasião em que um nobre discípulo através do conhecimento direto, aqui e agora, entra e permanece na libertação da mente e libertação através da sabedoria que são imaculadas com a destruição de todas as impurezas, nessa ocasião entre os devas esta expressão jubilosa é proclamada: 'Um nobre discípulo venceu a batalha com Mara. Ele esteve na frente da batalha e agora saiu vitorioso.' Essa é a terceira expressão jubilosa proclamada pelos devas de tempos em tempos nessa ocasião. </p>

<p class=Normal>"Essas, bhikkhus, são as três expressões jubilosas ... " </p>

<p class=Normal style='margin-left:.75in'> Vendo ter vencido a batalha<br>
até mesmo os devas o homenageiam, <br>
o discípulo do Perfeitamente Iluminado:</p>

<p class=Normal style='margin-left:.75in'> "Nós o saudamos, puro-sangue entre os homens, <br>
você venceu uma difícil batalha. <br>
Tendo destruído o exército da Morte, <br>
a sua libertação é completa."</p>

<p class=Normal style='margin-left:.75in'> Assim os devas o elogiam, <br>
aquele que realizou o objetivo, <br>
pois eles nele não percebem, <br>
razões para estar sujeito ao controle da Morte.
</p>


