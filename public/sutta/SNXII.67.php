<p class=Tit3 align=center style='text-align:center'><b>Samyutta Nikaya XII.67</b></Tit3>
<p class=Tit1 align=center style='text-align:center'>Nalakalapiyo Sutta </Tit1>
<p class=Tit1 align=center style='text-align:center'>Feixes de Junco </Tit1>

