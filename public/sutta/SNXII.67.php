<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em certa
ocasião o venerável Sariputta e o venerável MahaKotthita estavam próximos a Benares, no Parque do Gamo em Isipatana. Então ao anoitecer, saindo do
isolamento, o venerável MahaKotthita foi até o venerável Sariputta e ao chegar ambos se
cumprimentaram. Quando a conversa cortês e amigável havia terminado ele sentou
a um lado e disse para o venerável Sariputta: <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Agora
diga-me, Sariputta meu amigo: O envelhecimento e morte são produzidos pela
própria pessoa, ou são produzidos pelos outros, ou tanto pela própria pessoa
como pelos outros, ou surgem ao acaso sem serem criados pela própria pessoa e
tampouco pelos outros?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não é o
caso, meu amigo Kotthita, que o envelhecimento e morte são produzidos pela
própria pessoa, ou são produzidos pelos outros, ou tanto pela própria pessoa
como pelos outros, ou surgem ao acaso sem serem criados pela própria pessoa e
tampouco pelos outros. No entanto, do nascimento como condição, surge o
envelhecimento e morte.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Agora
diga-me, amigo Sariputta: O nascimento ... ser/existir .... apego ... desejo
... sensação ... contato ... as seis bases dos sentidos são produzidas pela
própria pessoa, ou são produzidas pelos outros, ou tanto pela própria pessoa
como pelos outros, ou surgem ao acaso sem serem criadas pela própria pessoa e
tampouco pelos outros?” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não é o
caso, meu amigo Kotthita, que as seis bases dos sentidos são produzidas pela
própria pessoa, ou são produzidas pelos outros, ou tanto pela própria pessoa
como pelos outros, ou surgem ao acaso sem serem criadas pela própria pessoa e
tampouco pelos outros. No entanto, da  mentalidade-materialidade (nome e forma)</span><span
lang=PT-BR style='mso-ansi-language:PT-BR'> como condição, surgem as seis bases
dos sentidos.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Agora
diga-me, amigo Sariputta: a mentalidade-materialidade (nome e forma)</span><span
lang=PT-BR style='mso-ansi-language:PT-BR'> é produzida pela própria pessoa, ou
é produzida pelos outros, ou tanto pela própria pessoa como pelos outros, ou
surge ao acaso sem ser criada pela própria pessoa e tampouco pelos outros?” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não é o
caso, meu amigo Kotthita, que a mentalidade-materialidade (nome e forma)</span><span
lang=PT-BR style='mso-ansi-language:PT-BR'> é produzida pela própria pessoa, ou
é produzida pelos outros, ou tanto pela própria pessoa como pelos outros, ou
surge ao acaso sem ser criada pela própria pessoa e tampouco pelos outros. No
entanto, da consciência como condição surge a  mentalidade-materialidade (nome e forma)</span><span
lang=PT-BR style='mso-ansi-language:PT-BR'>.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Agora
diga-me, amigo Sariputta: a consciência é produzida pela própria pessoa, ou é
produzida pelos outros, ou tanto pela própria pessoa como pelos outros, ou
surge ao acaso sem ser criada pela própria pessoa e tampouco pelos outros?” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não é o
caso, meu amigo Kotthita, que a consciência é produzida pela própria pessoa, ou
é produzida pelos outros, ou tanto pela própria pessoa como pelos outros, ou
surge ao acaso sem ser criada pela própria pessoa e tampouco pelos outros. No
entanto, da  mentalidade-materialidade (nome e forma)</span><span lang=PT-BR
style='mso-ansi-language:PT-BR'> como condição surge a consciência.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Agora
mesmo, amigo Sariputta, entendi a sua afirmação como, ‘Não é o caso, meu amigo
Kotthita, que a  mentalidade-materialidade (nome e forma)</span><span lang=PT-BR
style='mso-ansi-language:PT-BR'> é produzida pela própria pessoa, ou é
produzida pelos outros, ou tanto pela própria pessoa como pelos outros, ou
surge ao acaso sem ser criada pela própria pessoa e tampouco pelos outros. No
entanto, da consciência como condição surge a  mentalidade-materialidade (nome e forma)</span><span
lang=PT-BR style='mso-ansi-language:PT-BR'>.’ Mas em seguida entendi a sua
afirmação como, ‘Não é o caso, meu amigo Kotthita, que a consciência é
produzida pela própria pessoa, ou é produzida pelos outros, ou tanto pela
própria pessoa como pelos outros, ou surge ao acaso sem ser criada pela própria
pessoa e tampouco pelos outros. No entanto, da mentalidade-materialidade (nome e forma)</span><span
lang=PT-BR style='mso-ansi-language:PT-BR'> como condição surge a consciência.’
Agora, como deve ser entendido o significado dessas afirmações?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Muito bem
então, meu amigo Kotthita, eu explicarei com um símile pois alguns sábios
compreendem o significado de um enunciado através de um símile. É como se dois
feixes de junco estivessem em pé se apoiando um no outro. Da mesma maneira, da mentalidade-materialidade (nome e forma)</span><span
lang=PT-BR style='mso-ansi-language:PT-BR'> como condição, a consciência
[surge], da consciência como condição, a mentalidade-materialidade (nome e forma)
[surge]</span><span lang=PT-BR style='mso-ansi-language:PT-BR'>. Da mentalidade-materialidade (nome e forma)</span><span
lang=PT-BR style='mso-ansi-language:PT-BR'> como condição, as seis bases dos
sentidos. Das seis bases dos sentidos como condição, o contato. Do contato como
condição, a sensação. Da sensação como condição, o desejo. Do desejo como
condição, o apego. Do apego como condição, o ser/existir. Do ser/existir como
condição, o nascimento. Do nascimento como condição, então o envelhecimento e
morte, tristeza, lamentação, dor, angústia e desespero surgem. Essa é a origem
de toda essa massa de sofrimento.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se alguém
tirasse um dos feixes de junco, o outro cairia. Se alguém tirasse o outro, o
primeiro cairia. Da mesma maneira, da cessação da  mentalidade-materialidade (nome e forma),</span><span
lang=PT-BR style='mso-ansi-language:PT-BR'> cessa a consciência. Da cessação da
consciência, cessa a mentalidade-materialidade (nome e forma)</span><span lang=PT-BR
style='mso-ansi-language:PT-BR'>. Da cessação da mentalidade-materialidade (nome e forma),</span><span
lang=PT-BR style='mso-ansi-language:PT-BR'> cessam as seis bases dos sentidos.
Da cessação das seis bases dos sentidos, cessa o contato. Da cessação do
contato, cessa a sensação. Da cessação da sensação, cessa o desejo. Da cessação
do desejo, cessa o apego. Da cessação do apego, cessa o ser/existir. Da
cessação do ser/existir, cessa o nascimento. Da cessação do nascimento, então o
envelhecimento e morte, tristeza, lamentação, dor, angústia e desespero, tudo
cessa. Essa é a cessação de toda essa massa de sofrimento.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“É
impressionante, amigo Sariputta. É surpreendente, amigo Sariputta, quão bem
isso foi dito pelo venerável Sariputta. Eu me regozijo com o enunciado benéfico
formulado pelo venerável Sariputta com respeito a esses 36 tópicos. [<a href="#N1">1</a>]
<a name=R1></a>Se um bhikkhu ensina o Dhamma com o propósito do
desencantamento, desapego e cessação com respeito ao envelhecimento e morte,
ele merece ser chamado de um bhikkhu que é um orador do Dhamma. Se um bhikkhu
pratica o Dhamma com o propósito do desencantamento, desapego e cessação com
respeito ao envelhecimento e morte, ele merece ser chamado de um bhikkhu que
pratica de acordo com o Dhamma. Se, através do desencantamento, desapego e
cessação com respeito ao envelhecimento e morte, ele é libertado, então ele
merece ser chamado de um bhikkhu que realizou nibbana aqui e agora.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se um
bhikkhu ensina o Dhamma com o propósito do desencantamento, desapego e cessação
com respeito ao nascimento, ele merece ser chamado de um bhikkhu que é um
orador do Dhamma. Se um bhikkhu pratica o Dhamma com o propósito do
desencantamento, desapego e cessação com respeito ao nascimento, ele merece ser
chamado de um bhikkhu que pratica de acordo com o Dhamma. Se, através do
desencantamento, desapego e cessação com respeito ao nascimento, ele é
libertado, então ele merece ser chamado de um bhikkhu que realizou nibbana aqui e agora.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(Da mesma
forma com ser/existir, apego, desejo, sensação, contato, seis bases dos
sentidos, mentalidade-materialidade (nome e forma)</span><span lang=PT-BR style='mso-ansi-language:
PT-BR'> e consciência.)<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se um
bhikkhu ensina o Dhamma com o propósito do desencantamento, desapego e cessação
com respeito às formações volitivas ele merece ser chamado de um bhikkhu que é um orador
do Dhamma. Se um bhikkhu pratica o Dhamma com o propósito do desencantamento,
desapego e cessação com respeito às formações volitivas, ele merece ser chamado de um
bhikkhu que pratica de acordo com o Dhamma. Se, através do desencantamento,
desapego e cessação com respeito às formações volitivas, ele é libertado, então ele
merece ser chamado de um bhikkhu que realizou nibbana aqui e agora.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se um
bhikkhu ensina o Dhamma com o propósito do desencantamento, desapego e cessação
com respeito à ignorância ele merece ser chamado de um bhikkhu que é um orador
do Dhamma. Se um bhikkhu pratica o Dhamma com o propósito do desencantamento,
desapego e cessação com respeito à ignorância, ele merece ser chamado de um
bhikkhu que pratica de acordo com o Dhamma. Se, através do desencantamento,
desapego e cessação com respeito à ignorância, ele é libertado, então ele
merece ser chamado de um bhikkhu que realizou nibbana aqui e agora.<o:p></o:p></span></p>




