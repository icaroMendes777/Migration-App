<p class=Normal>Certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no Parque de Anathapindika. Lá ele se dirigiu aos monges desta forma: “Bhikkhus” – “Venerável Senhor,” eles responderam. O Abençoado disse o seguinte:</p> 

<p class=Normal> “Esses cinco são obstáculos, obstruções, corrupções da mente, enfraquecedores da sabedoria. Quais cinco? O desejo sensual é um obstáculo, uma obstrução, uma corrupção da mente, enfraquecedor da sabedoria. A má vontade é um obstáculo,  uma obstrução ... A preguiça e o torpor são um obstáculo, uma obstrução ... A inquietação e a ansiedade são um obstáculo, uma obstrução ... A dúvida é um obstáculo, uma obstrução, uma corrupção da mente,  enfraquecedora da sabedoria. E quando um bhikkhu não abandonou esses cinco obstáculos, obstruções, corrupções da mente, enfraquecedores da sabedoria, quando ele não possui força e tem fraco discernimento, que ele entenda aquilo que é benéfico para ele mesmo, que ele entenda aquilo que é benéfico para os outros, que ele entenda aquilo que é benéfico para ambos, que ele alcance um estado humano superior, que ele realize o verdadeiro nobre conhecimento e visão – isso é impossível.</p>
 
<p class=Normal> “Suponham que houvesse um rio, fluindo montanha abaixo, com rápida correnteza, arrastando tudo com as suas águas, e um homem abrisse canais perpendiculares em ambas margens de modo que a correnteza do rio fosse dispersada, espalhada e dissipada, sem que fluísse com rapidez e sem arrastar tudo com as suas águas. Do mesmo modo, quando um bhikkhu não abandonou esses cinco obstáculos, obstruções, corrupções da mente, enfraquecedores da sabedoria, quando ele não tem força e tem fraco discernimento, que ele entenda aquilo que é benéfico para ele mesmo, que ele entenda aquilo que é benéfico para os outros, que ele entenda aquilo que é benéfico para ambos, que ele alcance um estado humano superior, que ele realize o verdadeiro nobre conhecimento e visão – isso é impossível.</p>

<p class=Normal> “Agora, quando um bhikkhu abandonou esses cinco obstáculos, obstruções, corrupções da mente, enfraquecedores da sabedoria, quando ele tem forte discernimento, que ele entenda aquilo que é benéfico para ele mesmo, que ele entenda aquilo que é benéfico para os outros, que ele entenda aquilo que é benéfico para ambos, que ele alcance um estado humano superior, que ele realize o verdadeiro nobre conhecimento e visão – isso é possível. </p>

<p class=Normal> “Suponham que houvesse um rio, fluindo montanha abaixo, com rápida correnteza, arrastando tudo com as suas águas, e um homem fechasse os canais perpendiculares em ambas margens de modo que a correnteza do rio não fosse dispersada, não fosse espalhada e nem fosse dissipada, fluindo com rapidez e arrastando tudo com as suas águas. Do mesmo modo, quando um bhikkhu abandonou esses cinco obstáculos, obstruções, corrupções da mente, enfraquecedores da sabedoria, quando ele tem forte discernimento, que ele entenda aquilo que é benéfico para ele mesmo, que ele entenda aquilo que é benéfico para os outros, que ele entenda aquilo que é benéfico para ambos, que ele alcance um estado humano superior, que ele realize o verdadeiro nobre conhecimento e visão – isso é possível.”</p>

