<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, um
certo bhikkhu foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e
disse: “Venerável senhor, como é que alguém deve compreender, como é que alguém deve
ver, para que o entendimento incorreto seja abandonado?”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhu,
quando alguém compreende e vê o olho como impermanente, o entendimento incorreto é
abandonado. Quando alguém compreende e vê as formas como impermanentes ...
a consciência no olho como impermanente ... o contato no olho como impermanente ...
qualquer sensação que surja tendo o contato no olho como condição – quer seja prazerosa, 
dolorosa ou nem prazerosa, nem dolorosa - como impermanente, o entendimento incorreto é abandonado. <br> 
Quando alguém compreende e vê o ouvido como impermanente ... a mente como impermanente ... os objetos mentais como impermanentes ...
a consciência na mente como impermanente ... o contato na mente como impermanente ...
qualquer sensação que surja tendo o contato na mente como condição – quer seja prazerosa, 
dolorosa ou nem prazerosa, nem dolorosa - como impermanente, o entendimento incorreto é abandonado.É quando alguém compreende e vê
assim que o entendimento incorreto é abandonado.”<o:p></o:p></span></p>

