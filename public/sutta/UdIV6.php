<p class=Normal>Assim ouvi. Em certa ocasião o Abençoado estava em Savathi, no Bosque de Jeta, no Parque de Anathapindika.</p>



<p class=Normal>Agora, naquela ocasião o venerável Pindola Bharadvaja estava sentado não muito distante do Abençoado, com as pernas cruzadas e mantendo o corpo ereto. Pindola vivia na floresta, se alimentava de comida esmolada, vestia mantos feitos com trapos, se restringia a possuir não mais que três mantos, era modesto, contente, recluso, desapegado e desatado, esforçado e com a mente dotada de energia, defensor do ascetismo e dedicado à concentração da mente.</p>



<p class=Normal>O Abençoado então viu o venerável Pindola Bharadvaja sentado não muito distante, com as pernas cruzadas e mantendo o corpor ereto, um exemplo daqueles que vivem na floresta, se alimentam de comida esmolada, vestem mantos feitos com trapos, se restringem a possuir não mais que três mantos, modesto, contente, recluso, desapegado e desatado, esforçado e com a mente dotada de energia, defensor do ascetismo e dedicado à concentração da mente.</p>



<p class=Normal>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p>



	<p class=Normal style='margin-left:.75in'>"Não buscar defeitos, nem causar dano,<br>

	conter-se pelo patimokkha,<br>

	sendo moderado na alimentação,<br>

	recluso,<br>

	dedicado à mente superior:<br>

	esse é o ensinamento do Buda.”</p>

