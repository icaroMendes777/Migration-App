<p class=Normal> (64) "Bhikkhus, eu não vejo nenhuma outra única qualidade, que faz com que qualidades prejudiciais que ainda não surgiram surjam, e qualidades benéficas que já surgiram declinem, como o descontentamento. Para quem está descontente, as qualidades prejudiciais que ainda não surgiram surgem, e as qualidades benéficas que já surgiram declinam." </p>

