<p class=Normal> (615) "Bhikkhus, para o conhecimento direto da cobiça, sete coisas devem ser desenvolvidas. Quais sete? O fator da iluminação da atenção plena, o fator da iluminação da investigação dos fenômenos, o fator da iluminação da energia, o fator da iluminação da tranquilidade, o fator da iluminação da concentração, o fator da iluminação da equanimidade. Para o conhecimento direto da cobiça, essas sete coisas devem ser desenvolvidas." </p>

<p class=Normal> (616) "Bhikkhus, para o conhecimento direto da cobiça, sete coisas devem ser desenvolvidas. Quais sete? A percepção da impermanência, a percepção do não-eu, a percepção das coisas repulsivas no corpo, a percepção do perigo, a percepção de abandono, a percepção de desapego, a percepção da cessação. Para o conhecimento direto da cobiça, essas sete coisas devem ser desenvolvidas." </p>

<p class=Normal> (617) "Bhikkhus, para o conhecimento direto da cobiça, sete coisas devem ser desenvolvidas. Quais sete? A percepção das coisas repulsivas no corpo, a percepção da morte, a percepção das coisas repulsivas na comida, a percepção de não deleite com tudo no mundo, a percepção da impermanência, a percepção do sofrimento naquilo que é impermanente, e a percepção do não-eu naquilo que é sofrimento. Para o conhecimento direto da cobiça, essas sete coisas devem ser desenvolvidas." </p>

<p class=Normal> (618 - 644) "Bhikkhus, para a completa compreensão da cobiça ... para a completa destruição ... para o abandono ... para a destruição ... para o desaparecimento ... para o desapego ... para a cessação .... para abrir mão ... para a renúncia da cobiça, essas sete coisas devem ser desenvolvidas." </p>

<p class=Normal> (645 - 1124) "Bhikkhus, para o conhecimento direto ... para a completa compreensão .... para a completa destruição ... para o abandono ... para a destruição ... para o desaparecimento ... para o desapego ... para a cessação .... para abrir mão ... para a renúncia da raiva ... da delusão ... do ódio ... da hostilidade ... da difamação ... da insolência ... da inveja ... da avareza ... da enganação ... da astúcia ... da teimosia .... da veemência ... da presunção ... da arrogância ... da embriaguez .... da negligência, essas sete coisas devem ser desenvolvidas." </p>


