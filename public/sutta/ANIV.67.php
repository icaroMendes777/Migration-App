<p class=Normal>Certa ocasião o Abençoado estava em Savatthi, no Bosque de Jeta, no Parque de Anathapindika. Agora, naquela ocasião, em Savatthi um certo bhikkhu havia morrido depois de ter sido picado por uma cobra. Então, um grande número de bhikkhus foram até o Abençoado e depois de cumprimentá-lo sentaram a um lado e disseram: “Venerável senhor, agora mesmo em Savatthi, um certo bhikkhu morreu depois de ter sido picado por uma cobra.” </p>

<p class=Normal> “Então é certo, bhikkhus, que aquele bhikkhu não difundiu para as quatro linhagens reais de cobras uma mente imbuída de amor bondade. Pois se ele tivesse difundido para as quatro linhagens reais de cobras uma mente imbuída de amor bondade, ele não teria morrido depois de ter sido picado por uma cobra. Quais quatro? A linhagem real de cobras Virupakkha, [<a href="#N1">1</a>] <a name="R1 "> </a>a  linhagem real de cobras Erapatha, a linhagem real de cobras Chabyaputta, a linhagem real de cobras Gotamaka. É certo  que aquele bhikkhu não difundiu para as quatro linhagens reais de cobras uma mente imbuída de amor bondade. Pois se ele tivesse difundido para as quatro linhagens reais de cobras uma mente imbuída de amor bondade, ele não teria morrido depois de ter sido picado por uma cobra. Eu permito que vocês, bhikkhus, difundam para essas quatro linhagens reais de cobras uma mente imbuída de amor bondade com o propósito de auto-proteção, auto-preservação e auto-defesa.” </p>

<p class=Normal style='margin-left:.75in'>Eu tenho amor bondade pelas Virupakkhas,<br>
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;amor bondade pelas Erapathas, <br>
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;amor bondade pelas Chabyaputtas, <br>
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;amor bondade pelas Gotamakas.</p>

<p class=Normal style='margin-left:.75in'>Eu tenho amor bondade pelos seres sem patas, <br>
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;amor bondade pelos seres com duas patas, <br>
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;amor bondade pelos seres com quatro patas, <br>
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;amor bondade pelos seres com muitas patas. </p>

<p class=Normal style='margin-left:.75in'>Que os seres sem patas não me causem dano. <br>
Que os seres com duas patas não me causem dano. <br>
Que os seres com quatro patas não me causem dano. <br>
Que os seres com muitas patas não me causem dano. </p>

<p class=Normal style='margin-left:.75in'>Que todas as criaturas, <br>
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;todos os seres que respiram, <br>
		&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;todos os seres<br>
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;— todos sem exceção — <br>
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;desfrutem de boa fortuna. <br>
Que nenhum deles sofra nenhum dano. </p>

<p class=Normal style='margin-left:.75in'><a name="R2"> </a>Ilimitado é o Buda, <br>
ilimitado é o Dhamma, <br>
ilimitado é a Sangha. <br>
Há um limite para as criaturas rastejantes: <br>
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;cobras, escorpiões, centopéias, <br>
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;aranhas, lagartos e ratos. <br>
Eu fiz esta salvaguarda, <br>
eu fiz esta proteção. <br>
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Que essas criaturas se retirem. <br>
Eu me curvo ante<br>
o Abençoado, <br>
homenageio os sete Budas supremos.[ <a href="#N2">2</a>] </p>

