compreender completamente a mente como esta na verdade é. Eles também
permanecem contemplando os objetos mentais como objetos mentais ... para
compreender completamente os objetos mentais como estes na verdade são.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
