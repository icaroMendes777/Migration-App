<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. [<a href="#N1">1</a>] <a name=R1></a>Em certa ocasião o Abençoado estava
em Savatthi no Bosque de Jeta, no Parque de Anathapindika. Lá o venerável
Sariputta se dirigiu aos monges desta forma: “Amigos, bhikkhus”. – “Amigo”,
eles responderam. O venerável Sariputta<span style="mso-spacerun: yes"> 
</span>disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><a name=T2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2. “Amigos, da mesma forma como a pegada de qualquer ser vivo que
caminha pode ser colocada dentro da pegada de um elefante e assim a pegada do
elefante é declarada como a líder delas devido ao seu grande tamanho; assim
também todos bons ensinamentos podem ser incluídos nas Quatro Nobres Verdades.
[</span><a href="#N2"><span lang=PT-BR style='mso-ansi-language:PT-BR'>2</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R2></a>Quais quatro? Na
nobre verdade do sofrimento, na nobre verdade da origem do sofrimento, na nobre
verdade da cessação do sofrimento e na nobre verdade do caminho que conduz à
cessação do sofrimento.<o:p></o:p></span></p>



<p class=Normal><a name=T3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>3. “E qual é a nobre verdade do sofrimento? Nascimento é sofrimento;
envelhecimento é sofrimento; enfermidade é sofrimento; morte é sofrimento;
tristeza, lamentação, dor, angústia e desespero são sofrimento; não obter o que
se deseja é sofrimento; em resumo, os cinco agregados influenciados pelo apego
são sofrimento.<o:p></o:p></span></p>



<p class=Normal><a name=T4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>4. “E quais são os cinco agregados influenciados pelo apego? Eles são: o
agregado da forma material influenciado pelo apego, o agregado da sensação
influenciado pelo apego, o agregado da percepção influenciado pelo apego, o
agregado das formações volitivas influenciado pelo apego e o agregado da consciência
influenciado pelo apego.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. “E o que
é o agregado da forma material influenciado pelo apego? São os quatro grandes
elementos e a forma material derivada dos quatro grandes elementos. E quais são
os quatro grandes elementos? Eles são o elemento terra, o elemento água, o
elemento fogo e o elemento ar.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(O ELEMENTO
TERRA)</b><o:p></o:p></span></p>



<p class=Normal><a name=T6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>6. “O que, amigos, é o elemento terra? O elemento terra pode ser interno
ou externo. O que é o elemento terra interno? Qualquer coisa interna que
pertence à pessoa, que seja sólida, solidificada e pela qual exista apego, isto
é, cabelos, pêlos do corpo, unhas, dentes, pele, carne, tendões, ossos, medula,
rins, coração, fígado, diafragma, baço, pulmões, intestino grosso, intestino
delgado, conteúdo do estômago, fezes ou qualquer outra coisa interna que
pertença à pessoa, que seja sólida, solidificada e pela qual exista apego: a
isto se chama o elemento terra interno. [<a href="#N3">3</a>] <a name=R3></a>Agora,
tanto o elemento terra interno como o elemento terra externo são simplesmente
elementos terra. [<a href="#N4">4</a>] <a name=R4></a>E isso deve ser visto
como na verdade é, com correta sabedoria: ‘Isso não é meu, isso não sou eu,
isso não é o meu eu.’ Quando a pessoa vê dessa forma, como na verdade é, com
correta sabedoria, a pessoa fica desencantada com o elemento terra e faz com
que a mente fique desapegada<span style="mso-spacerun: yes">  </span>em relação
ao elemento terra. <span style='color:red'><o:p></o:p></span></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. “Agora
um tempo virá em que o elemento terra externo será perturbado e então o
elemento terra externo desaparecerá. [</span><a href="#N5"><span lang=PT-BR
style='mso-ansi-language:PT-BR'>5</span></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>] <a name=R5></a>Se até mesmo esse elemento terra externo, sendo tão
vasto, é visto como impermanente, sujeito à destruição, desaparecimento e
mudança, o que dizer deste corpo, que é agarrado pelo desejo e que sobrevive
por pouco tempo? Não há como considerar isso como ‘eu’ ou ‘meu’ ou ‘eu sou.’ [</span><a
href="#N6"><span lang=PT-BR style='mso-ansi-language:PT-BR'>6</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=R6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>8. “Pois então, se outros ofendem, insultam, criticam e molestam um
bhikkhu [que viu esse elemento tal como ele na verdade é], ele compreende
assim: ‘Essa sensação dolorosa nascida do contato no ouvido surgiu em mim. Isso
é dependente, não independente. Dependente de que? Dependente do contato.’ [</span><a
href="#N7"><span lang=PT-BR style='mso-ansi-language:PT-BR'>7</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R7></a>Então ele vê que
aquele contato é impermanente, que aquela sensação é impermanente, que aquela
percepção é impermanente, que aquelas formações volitivas são impermanentes [</span><a
href="#N7a"><span lang=PT-BR style='mso-ansi-language:PT-BR'>7a</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R7a></a> e que aquela
consciência é impermanente. E a sua mente, usando o elemento como objeto de meditação, penetra esse elemento e adquire
confiança, se estabiliza e se liberta. [</span><a href="#N8"><span lang=PT-BR
style='mso-ansi-language:PT-BR'>8</span></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=R8></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>9. “Agora, se outros atacarem aquele bhikkhu de um jeito que seja
indesejável e desagradável, através do contato com punhos, pedras, paus e
facas, ele compreende assim: ‘Este corpo tem uma natureza tal que o contato com
punhos, pedras, paus e facas o agridem. [</span><a href="#N9"><span lang=PT-BR
style='mso-ansi-language:PT-BR'>9</span></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>] <a name=R9></a>Mas isto foi dito pelo Abençoado com o seu conselho
sobre o “símile da serra”: “Bhikkhus, mesmo se bandidos decepassem com
selvageria os seus membros, um a um, com uma serra, aquele que fizer surgir uma
mente cheia de raiva em relação a eles não estará praticando os meus
ensinamentos.” [</span><a href="#N10"><span lang=PT-BR style='mso-ansi-language:
PT-BR'>10</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>] <a
name=R10></a>Portanto, uma energia incansável deve ser estimulada em mim e uma
persistente atenção plena deve ser estabelecida, meu corpo ficará tranqüilo e
sossegado, minha mente concentrada e unificada. E agora, que o contato com
punhos, pedras, paus e facas agridam este corpo, pois é exatamente assim que os
ensinamentos do Buda são praticados.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. “Quando
aquele bhikkhu se recorda do Buda, do Dhamma e da Sangha, se a equanimidade
suportada por aquilo que é benéfico não se estabelece nele, ele desperta um
senso de urgência desta forma: ‘É uma perda para mim, não é um ganho para mim,
é ruim para mim, não é bom para mim, que quando eu me recorde do Buda, do
Dhamma e da Sangha, a equanimidade suportada por aquilo que é benéfico não se
estabeleça em mim.’ [</span><a href="#N11"><span lang=PT-BR style='mso-ansi-language:
PT-BR'>11</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>] <a
name=R11></a>Como quando uma nora vê o seu sogro, ela desperta um senso de
urgência [em agradá-lo], assim também, quando aquele bhikkhu então se recorda
do Buda, do Dhamma e da Sangha, se a equanimidade suportada por aquilo que é
benéfico não se estabelece nele, então ele desperta um senso de urgência. Mas
se quando ele então se recorda do Buda, do Dhamma e da Sangha e a equanimidade
suportada por aquilo que é benéfico se estabelece nele, ele então fica
satisfeito com isso. Nesse ponto, amigos, muito foi realizado por aquele
bhikkhu.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(O ELEMENTO
ÁGUA)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. “O que,
amigos, é o elemento água? O elemento água pode ser interno ou externo. Qual é
o elemento água interno? Qualquer coisa interna que pertence à pessoa, que seja
liquida, aquosa e pela qual existe apego, isto é, bílis, fleuma, pus, sangue,
suor, gordura, lágrimas, óleo, saliva, muco, líquido sinovial, urina ou
qualquer outra coisa interna na pessoa, que seja liquida, aquosa e pela qual
exista apego: a isto se chama o elemento água interno. Agora tanto o elemento
água interno como o elemento água externo são simplesmente elementos água. E
isso deve ser visto como na verdade é, com correta sabedoria: ‘Isso não é meu,
isso não sou eu, isso não é o meu eu.’ Quando a pessoa vê dessa forma, como na
verdade é, com correta sabedoria, a pessoa fica desencantada com o elemento
água e faz com que a mente fique desapegada em relação ao elemento água.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. “Agora,
um tempo virá em que o elemento água externo será perturbado. Ele arrastará
vilarejos, vilas, cidades, distritos e países. Um tempo virá em que as águas
neste grande planeta terra descerão até cem léguas, duzentas léguas, trezentas
léguas, quatrocentas léguas, quinhentas léguas, seiscentas léguas, setecentas
léguas. Um tempo virá em que as águas no grande oceano terão sete palmos de
profundidade, seis palmos de profundidade ... dois palmos de profundidade, apenas
um palmo de profundidade. Um tempo virá
em que as águas no grande oceano terão sete braçadas de profundidade, seis
braçadas de profundidade ... duas braçadas de profundidade, apenas uma braçada de
profundidade. Um tempo virá em que as águas no grande oceano terão meia braçada
de profundidade, uma profundidade até a cintura, uma<span style="mso-spacerun:
yes">  </span>profundidade até o joelho, uma profundidade até o tornozelo. Um
tempo virá em que as águas no grande oceano não serão suficientes nem mesmo
para molhar a articulação de um dedo. Se até mesmo esse elemento água externo,
sendo tão vasto, é visto como impermanente, sujeito à destruição,
desaparecimento e mudança, o que dizer deste corpo, que é agarrado pelo desejo
e que sobrevive por pouco tempo? Não há como considerar isso como ‘eu’ ou ‘meu’
ou ‘eu sou.’ <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13-15.
“Pois então, se outros ofendem, insultam, criticam e molestam um bhikkhu [que
viu esse elemento tal como ele na verdade é], ele compreende assim: ... (repetir
versos 8-10) ... Nesse ponto também, amigos, muito foi realizado por aquele
bhikkhu.<o:p></o:p></span></p>



<p class=Normal><b>(O ELEMENTO FOGO)</b></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16. “O que,
amigos, é o elemento fogo? O elemento fogo pode ser interno ou externo. Qual é
o elemento fogo interno? Qualquer coisa interna que pertence à pessoa, que seja
fogo, ardente e pela qual exista apego, isto é, aquilo pelo qual a pessoa é
aquecida, envelhece e é consumida, aquilo pelo qual o que é comido, bebido,
consumido e saboreado é digerido da maneira adequada ou qualquer outra coisa
interna na pessoa, que seja fogo, ardente e pela qual exista apego: é chamada
de elemento fogo interno. Agora, tanto o elemento fogo interno como o elemento
fogo externo são simplesmente elementos fogo. E isso deve ser visto como na
verdade é, com correta sabedoria: ‘Isso não é meu, isso não sou eu, isso não é
o meu eu.’ Quando a pessoa vê dessa forma, como na verdade é, com correta
sabedoria, a pessoa fica desencantada com o elemento fogo e faz com que a mente
fique desapegada em relação ao elemento fogo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17. “Agora,
um tempo virá em que o elemento fogo externo será perturbado. Ele queimará
vilarejos, vilas, cidades, distritos e países. Ele se extinguirá devido à falta
de combustível ao encontrar capim verde ou uma estrada, ou uma pedra, ou a
água, ou um amplo espaço aberto. Um tempo virá em que se buscará acender um
fogo até mesmo com penas de galo e raspas de peles de animais. Se até mesmo
esse elemento fogo externo, sendo tão vasto, é visto como impermanente, sujeito
à destruição, desaparecimento e mudança, o que dizer deste corpo, que é
agarrado pelo desejo e que sobrevive por pouco tempo? Não há como considerar
isso como ‘eu’ ou ‘meu’ ou ‘eu sou.’ <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18-20.
“Pois então, se outros ofendem, insultam, criticam e molestam um bhikkhu [que
viu esse elemento tal como ele na verdade é], ele compreende assim: ... (repetir
versos 8-10) ... Nesse ponto também, amigos, muito foi realizado por aquele
bhikkhu.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(O ELEMENTO
AR)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>21. “O que,
amigos, é o elemento ar? O elemento ar pode ser interno ou externo. Qual é o
elemento ar interno ? Qualquer coisa interna, que pertence à pessoa, que seja
ar, arejada e pela qual exista apego, isto é, ventos que sobem, ventos que descem,
ventos no estômago, ventos nos intestinos, ventos que percorrem o corpo, a
inspiração e a expiração ou qualquer outra coisa interna na pessoa que seja ar,
arejada e pela qual exista apego: é chamada de elemento ar interno. Agora,
tanto o elemento ar interno como o elemento ar externo são simplesmente
elementos ar. E isso deve ser visto como na verdade é, com correta sabedoria:
‘Isso não é meu, isso não sou eu, isso não é o meu eu.’ Quando a pessoa vê
dessa forma, como na verdade é, com correta sabedoria, a pessoa fica
desencantada com o elemento ar e faz com que a mente fique desapegada em
relação ao elemento ar.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style="mso-spacerun: yes"> </span><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>22. “Agora,
um tempo virá em que o elemento ar externo será perturbado. Ele arrastará
vilarejos, vilas, cidades, distritos e países. Um tempo virá em que se buscará
vento no último mês da estação quente por meio de um leque ou um fole. E até
mesmo os filamentos de palha nas franjas dos telhados não se moverão. Se até
mesmo esse elemento ar externo, sendo tão vasto, é visto como impermanente, sujeito
à destruição, desaparecimento e mudança, o que dizer deste corpo, que é
agarrado pelo desejo e que sobrevive por pouco tempo? Não há como considerar
isso como ‘eu’ ou ‘meu’ ou ‘eu sou.’ <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>23-25.
“Pois então, se outros ofendem, insultam, criticam e molestam um bhikkhu [que
viu esse elemento tal como ele na verdade é], ele compreende assim: ... (repetir
versos 8-10) ... Nesse ponto também, amigos, muito foi realizado por aquele
bhikkhu.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>26.
“Amigos, do mesmo modo como um espaço enclausurado por madeira, capim e barro
passa a ser chamado de ‘casa’; um espaço, quando enclausurado por ossos e
tendões, carne e pele, também passa a ser chamado de ‘forma material.’ [</span><a
href="#N12"><span lang=PT-BR style='mso-ansi-language:PT-BR'>12</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=R12></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>27. “Se, amigos, no interior o olho está íntegro, mas nenhuma forma
externa surge no campo de visão e não há o correspondente engajamento [da
consciência], então não há a manifestação da seção correspondente da
consciência. [</span><a href="#N13"><span lang=PT-BR style='mso-ansi-language:
PT-BR'>13</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>] <a
name=R13></a>Se no interior o olho está íntegro e formas externas surgem no campo
de visão, mas não há o correspondente engajamento [da consciência], então não
há a manifestação da seção correspondente da consciência. Mas se no interior o
olho está íntegro e formas externas surgem no campo de visão e há o
correspondente engajamento [da consciência], então há a manifestação da seção
correspondente da consciência. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style="mso-spacerun: yes"> </span><o:p></o:p></span></p>

<p class=Normal><a name=T28></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>28. “A forma material que assim surge é parte do agregado da
forma material influenciado pelo apego. [</span><a href="#N14"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>14</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>] <a name=R14></a>A sensação que assim
surge é parte do agregado da sensação influenciado pelo apego. A percepção que
assim surge é parte do agregado
da percepção influenciado pelo apego. As formações volitivas que assim surgem são
parte do agregado das formações volitivas influenciado pelo apego. A consciência que
assim surge é parte do agregado da consciência influenciado pelo apego.
Ele compreende assim: ‘Assim é, de fato, como ocorre a inclusão, reunião e
acumulação de coisas nesses cinco agregados influenciados pelo apego. Agora,
isso foi dito pelo Abençoado: “Quem vê a origem dependente vê o Dhamma; quem vê
o Dhamma vê a origem dependente.” [</span><a href="#N15"><span lang=PT-BR
style='mso-ansi-language:PT-BR'>15</span></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>] <a name=R15></a>E esses cinco agregados influenciados pelo apego têm
origem dependente. O desejo, atração, agarração e o apego por esses
cinco agregados influenciados pelo apego são a origem do sofrimento. [</span><a
href="#N16"><span lang=PT-BR style='mso-ansi-language:PT-BR'>16</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R16></a>A remoção do
desejo e cobiça, o abandono do desejo e cobiça por esses cinco agregados
influenciados pelo apego é a cessação do sofrimento.’ Nesse ponto também,
amigos, muito foi realizado por aquele bhikkhu.[</span><a href="#N17"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>17</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>] <o:p></o:p></span></p>



<p class=Normal><a name=R17></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>29-30. “Se, amigos, no interior o ouvido está íntegro, mas nenhum som
externo surge no campo de audição ... (igual aos versos 27-28) ... Nesse ponto
também, amigos, muito foi realizado por aquele bhikkhu.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>31-32. “Se,
amigos, no interior o nariz está íntegro, mas nenhuma aroma externo surge no
campo do olfato ... Nesse ponto também, amigos, muito foi realizado por aquele
bhikkhu.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>33-34. “Se,
amigos, no interior a língua está íntegra, mas nenhum sabor externo surge no
campo do paladar ... Nesse ponto também, amigos, muito foi realizado por aquele
bhikkhu.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>35-36. “Se,
amigos, no interior o corpo está íntegro, mas nenhum tangível externo surge no
seu campo ... Nesse ponto também, amigos, muito foi realizado por aquele bhikkhu.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>37. “Se,
amigos, no interior a mente está íntegra, mas nenhum objeto mental
surge no seu campo e não há o correspondente engajamento [da consciência],
então não há a manifestação da seção correspondente da consciência. [</span><a
href="#N18"><span lang=PT-BR style='mso-ansi-language:PT-BR'>18</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R18></a>Se no interior a
mente está íntegra e objetos<span style="mso-spacerun: yes">  </span>mentais
surgem no seu campo, mas não há o correspondente engajamento [da
consciência], então não há a manifestação da seção correspondente da
consciência. [</span><a href="#N19"><span lang=PT-BR style='mso-ansi-language:
PT-BR'>19</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>] <a
name=R19></a>Mas se no interior a mente está íntegra e objetos mentais
surgem no seu campo e há o correspondente engajamento [da consciência], então
há a manifestação da seção correspondente da consciência.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>38. “A
forma material que assim surge é parte do agregado da forma material
influenciado pelo apego. A sensação que assim surge é parte do agregado
da sensação influenciado pelo apego. A percepção que assim surge é
parte do agregado da percepção influenciado pelo apego. As formações volitivas assim surgem são parte do agregado das formações volitivas influenciado pelo apego. A
consciência que assim surge é parte do agregado da consciência
influenciado pelo apego. Ele compreende assim: ‘Assim é, de fato, como ocorre a
inclusão, reunião e acumulação de coisas nesses cinco agregados influenciados
pelo apego. Agora, isso foi dito pelo Abençoado: “Quem vê a origem dependente
vê o Dhamma; quem vê o Dhamma vê a origem dependente.” E esses cinco agregados
influenciados pelo apego têm origem dependente. A aspiração, paixão, atração e
a agarração por esses cinco agregados influenciados pelo apego são a
origem do sofrimento. A remoção do desejo e cobiça, o abandono do desejo e
cobiça por esses cinco agregados influenciados pelo apego é a cessação do
sofrimento.’ Nesse ponto também, amigos, muito foi realizado por aquele
bhikkhu.”<o:p></o:p></span></p>



<p class=Normal><a name=R31></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Isso foi o que disse o venerável Sariputta. Os bhikkhus ficaram satisfeitos
e contentes com as palavras do venerável Sariputta.<o:p></o:p></span></p>


