objetivo de ser abrangente e para comparação (v. 6-7). Cada um dos elementos é
exposto como base para a meditação de insight, bem como para desenvolver
paciência, convicção e equanimidade (v. 8-10). Tendo concluído o exame dos
elementos, o Ven. Sariputta em seguida toma os aspectos das Quatro Nobres
