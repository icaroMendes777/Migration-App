<p class=Normal style='margin-left:.75in'>



<p class=Normal> Ouvi que em certa ocasião o Abençoado estava entre os Bhaggas, em Sumsumaragira,
no Bosque de Bhesakala, no Parque do Gamo. Então, o chefe de família Nakulapita
foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse:<o:p></o:p></span></p>


<p class=Normal> “Senhor, eu sou um homem velho, envelhecido, com a idade avançada, pressionado pelos anos, avançado na vida, chegando ao último estágio. Eu sou atormentado pelo corpo e sofro com
cada momento. E muito raramente eu vejo o Abençoado e os bhikkhus que nutrem o
coração. Possa o Abençoado ensinar-me, possa o Abençoado instruir-me, para o
meu bem-estar e felicidade por muito tempo.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR>“Assim é, chefe de família. Assim é. O corpo é sofrimento,
fraco e atormentado. Se qualquer um que com esse corpo declarasse, por
um momento que seja, ter verdadeira saúde,<span style="mso-spacerun: yes"> 
</span>a que outra razão deveríamos atribuir isso, senão à tolice? Portanto,
chefe de família, assim você deve praticar:<span style="mso-spacerun: yes"> 
</span>‘Mesmo que eu sofra no meu corpo, minha mente não sofrerá.’ Assim é como
você deve praticar.” </span></p>

<p class=Normal><span lang=PT-BR>Então, o chefe de família Nakulapita, satisfeito e contente
com as palavras do Abençoado, levantou-se do seu assento e depois de homenagear
o Abençoado, mantendo-o à sua direita, partiu. Ele então foi até o Ven.
Sariputta e depois de cumprimentá-lo sentou a um lado e o Ven. Sariputta disse
o seguinte: </span></p>

<p class=Normal><span lang=PT-BR>“Suas faculdades estão serenas, chefe de família, sua
complexão está pura e brilhante. Você teve a oportunidade de ouvir hoje um
discurso do Dhamma na presença do Abençoado?” </span></p>

<p class=Normal><span lang=PT-BR>“Como poderia ser de outra forma, venerável senhor?<span
style="mso-spacerun: yes">  </span>Eu acabo de receber do Abençoado a ambrosia
de um discurso do Dhamma.” </span></p>

<p class=Normal><span lang=PT-BR>“E como você recebeu do Abençoado a ambrosia de um discurso
do Dhamma.?”<span style="mso-spacerun: yes">  </span></span></p>

<p class=Normal> “Agora mesmo fui até o Abençoado e depois de cumprimentá-lo sentei a um
lado e disse: <o:p></o:p></span></p>


<p class=Normal> ‘Senhor, eu sou um homem velho, envelhecido, com a idade avançada, pressionado pelos anos, avançado na vida, chegando ao último estágio. Eu sou atormentado pelo corpo e sofro com
cada momento. E<span style="mso-spacerun: yes">  </span>muito raramente eu vejo
o Abençoado e os bhikkhus que nutrem o coração. Possa o Abençoado ensinar-me,
possa o Abençoado instruir-me, para o meu bem-estar e felicidade por muito
tempo.’”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR>“Quando isto foi dito, o Abençoado disse: ‘Assim é, chefe
de família. Assim é. O corpo é sofrimento, fraco e atormentado. Se qualquer
um que com esse corpo declarasse, por um momento que seja, ter verdadeira
saúde, a que outra razão deveríamos atribuir isso, senão à tolice? Portanto,
chefe de família, assim você deve praticar:<span style="mso-spacerun: yes"> 
</span>“Mesmo que eu sofra no meu corpo, minha mente não sofrerá.” Assim é como
você deve praticar. Assim é como recebi do Abençoado a ambrosia de um discurso
do Dhamma.’”</span></p>

<p class=Normal><span lang=PT-BR>Mas, porque não lhe ocorreu questionar mais o Abençoado:
‘De que forma alguém sofre no corpo e sofre na mente? E de que forma alguém
sofre no corpo, porém não sofre na mente?’ </span></p>

<p class=Normal><span lang=PT-BR>“Eu viria de uma grande distância para ouvir a explicação
dessas palavras na presença do Ven. Sariputta. Seria bom se o próprio Ven.
Sariputta me esclarecesse isso.” </span></p>

<p class=Normal><span lang=PT-BR>&quot;Então, nesse caso, chefe de família, ouça e preste
muita atenção àquilo que eu vou dizer.” -<span style="mso-spacerun: yes"> 
</span>“Sim, venerável senhor,” o chefe de família Nakulapita respondeu. O Ven.
Sariputta disse o seguinte: </span></p>

<p class=Normal><span lang=PT-BR>“Como, chefe de família, alguém sofre no corpo e sofre na
mente? É o caso em que uma pessoa comum sem instrução que não respeita os
nobres, que não é proficiente nem disciplinada no Dhamma deles, que não
respeita os homens verdadeiros, que não é proficiente nem disciplinada no
Dhamma deles, supõe que a forma é o eu, ou o eu é possuído de forma, ou a forma
como estando no eu, ou o eu como estando na forma. Ela está obcecada com a
idéia de que ‘Eu sou a forma’ ou ‘A forma é minha.’ Enquanto ela está obcecada
com essas idéias, sua forma muda e se altera. Com a mudança e alteração da
forma, surgem nela a tristeza, lamentação, dor, angústia e desespero. </span></p>

<p class=Normal><span lang=PT-BR>“Ela supõe que a sensação é o eu, ou o eu é possuído de
sensação, ou a sensação como estando no eu, ou o eu como estando na sensação.
Ela está obcecada com a idéia de que ‘Eu sou a sensação’ ou ‘A sensação é
minha.’ Enquanto ela está obcecada com essas idéias, sua sensação muda e se
altera. Com a mudança e alteração da sensação, surgem nela a tristeza,
lamentação, dor, angústia e desespero. </span></p>

<p class=Normal><span lang=PT-BR>“Ela supõe que a percepção é o eu, ou o eu é possuído de
percepção, ou a percepção como estando no eu, ou o eu como estando na
percepção. Ela está obcecada com a idéia de que ‘Eu sou a percepção’ ou ‘A
percepção é minha.’ Enquanto ela está obcecada com essas idéias, sua percepção
muda e se altera. Com a mudança e alteração da percepção, surgem nela a
tristeza, lamentação, dor, angústia e desespero.</span></p>

<p class=Normal><span lang=PT-BR>“Ela supõe que as formações volitivas são o eu, ou o eu é possuído de
formações volitivas, ou as formações volitivas como estando no eu, ou o eu como estando nas
formações volitivas. Ela está obcecada com a idéia de que ‘Eu sou as formações volitivas’ ou ‘As
formações volitivas são minhas.’ Enquanto ela está obcecada com essas idéias, suas
formações volitivas mudam e se alteram. Com a mudança e alteração das formações volitivas surgem
nela a tristeza, lamentação, dor, angústia e desespero. </span></p>

<p class=Normal><span lang=PT-BR>&quot;Ela supõe que a consciência é o eu, ou o eu é
possuído de consciência, ou a consciência como estando no eu, ou o eu como
estando na consciência. Ela está obcecada com a idéia de que ‘Eu sou a
consciência’ ou ‘A consciência é minha.’ Enquanto ela está obcecada com essas
idéias, sua consciência muda e se altera. Com a mudança e alteração da
consciência surgem nela a tristeza, lamentação, dor, angústia e desespero. </span></p>

<p class=Normal><span lang=PT-BR>“Assim, chefe de família, é como alguém sofre no corpo e na
mente. </span></p>

<p class=Normal><span lang=PT-BR>“E como que alguém sofre no corpo, porém não sofre na
mente? É o caso em que um nobre discípulo bem instruído que respeita os nobres,
que é proficiente e disciplinado no Dhamma deles, que respeita os homens verdadeiros,
que é proficiente e disciplinado no Dhamma deles não supõe que forma é o eu, ou
o eu é possuído de forma, ou a forma como estando no eu, ou o eu como estando
na forma. Ele não está obcecado com a idéia de que ‘Eu sou a forma’ ou ‘A forma
é minha.’ Enquanto ele não está obcecado com essas idéias, sua forma muda e se
altera. Com a mudança e alteração da forma não surgem nele a tristeza,
lamentação, dor, angústia e desespero. </span></p>

<p class=Normal><span lang=PT-BR>“Ele não supõe que a sensação é o eu... </span></p>

<p class=Normal><span lang=PT-BR>“Ele não supõe que a percepção é o eu... </span></p>

<p class=Normal><span lang=PT-BR>“Ele não supõe que as formações volitivas são o eu... </span></p>

<p class=Normal><span lang=PT-BR>“Ele não supõe que a consciência é o eu, ou o eu é possuido
de consciência, ou a consciência como estando no eu, ou o eu como estando na
consciência. Ele não está obcecado com a idéia de que 'Eu sou a consciência' ou
'A consciência é minha.' Enquanto ele não está obcecado com essas idéias, sua
consciência muda e se altera. Com a mudança e alteração da consciência não
surgem nele a tristeza, lamentação, dor, angústia e desespero. </span></p>

<p class=Normal><span lang=PT-BR>“Assim, chefe de família, é como alguém sofre no corpo,
porém não sofre na mente.”</span></p>

<p class=Normal><span lang=PT-BR>Isso foi o que o disse o Ven. Sariputta. O chefe de família
Nakulapita ficou satisfeito e contente com as palavras do Ven.
Sariputta. </span></p>




