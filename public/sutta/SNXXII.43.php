<p class=Normal> Em Savatthi. </p>

<p class=Normal> "Bhikkhus, sejam ilhas para vocês mesmos, refúgios para vocês mesmos, não buscando nenhum refúgio externo; com o Dhamma como a sua ilha, o Dhamma como refúgio, buscando nenhum outro refúgio, assim deve ser investigado: 'Do que nascem a tristeza, lamentação, dor, angústia e desespero? Como são produzidos?'</p>

<p class=Normal> "Bhikkhus, do que nascem a tristeza, lamentação, dor, angústia e desespero? Como são produzidos? Aqui, bhikkhus, a pessoa comum sem instrução, que não respeita os nobres, que não é proficiente nem treinada no Dhamma deles, que não respeita os homens verdadeiros, que não é proficiente nem treinada no Dhamma deles, considera que a forma é o eu, considera o eu como possuído de forma material, considera a forma material como estando no eu, considera o eu como estando na forma material. Essa forma muda e se altera. Com a forma mudando e se alterando a tristeza, lamentação, dor, angústia e desespero surgem nele. </p>

<p class=Normal> "Ele considera que a sensação é o eu ... considera que a percepção é o eu ... considera que as formações volitivas são o eu ... considera que a consciência é o eu, considera o eu como possuído de consciência, considera a consciência como estando no eu, consciência o eu como estando na consciência. Essa consciência muda e se altera. Com a consciência mudando e se alterando a tristeza, lamentação, dor, angústia e desespero surgem nele. </p>

<p class=Normal> "Mas, bhikkhus, quando alguém compreendeu a impermanência da forma, a sua mudança, desaparecimento, e cessação, e quando alguém vê com correta sabedoria: 'No passado a também agora toda forma é impermanente, sofrimento, e sujeita à mudança,' então a tristeza, lamentação, dor, angústia e desespero são abandonadas. Com o seu abandono, ele não fica agitado. Sem agitação, ele permanece feliz. Um bhikkhu que permanece feliz é dito ter sido apaziguado nesse aspecto. </p>

<p class=Normal> "Mas, bhikkhus, quando alguém compreendeu a impermanência da sensação ... percepção ... formações volitivas ... consciência, a sua mudança, desaparecimento, e cessação, e quando alguém vê com correta sabedoria: 'No passado a também agora toda consciência é impermanente, sofrimento, e sujeita à mudança,' então a tristeza, lamentação, dor, angústia e desespero são abandonadas. Com o seu abandono, ele não fica agitado. Sem agitação, ele permanece feliz. Um bhikkhu que permanece feliz é dito ter sido apaziguado nesse aspecto." </p>


