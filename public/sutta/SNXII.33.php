<p class=Normal> Em Savatthi. "Bhikkhus, eu lhes ensinarei quarenta e quatro tipos de conhecimento. Ouçam e prestem muita atenção àquilo que eu vou dizer.” – “Sim, venerável senhor,” os bhikkhus responderam. O Abençoado disse o seguinte: </p>


<p class=Normal>"Bhikkhus, quais são os quarenta e quatro tipos de conhecimento? Conhecimento do envelhecimento e morte, conhecimento da sua origem, conhecimento da sua cessação, conhecimento do caminho que conduz à sua cessação. Conhecimento do nascimento ... conhecimento do ser/existir ... conhecimento do apego ... conhecimento do desejo ... conhecimento da sensação  ... conhecimento do contato ... conhecimento das seis bases  ... conhecimento da mentalidade-materialidade (nome e forma) ... conhecimento da consciência ... conhecimento das formações volitivas, conhecimento da sua origem, conhecimento da sua cessação, conhecimento do caminho que conduz à sua cessação. Esses, bhikkhus são os quarenta e quatro tipos de conhecimento. </p>


<p class=Normal>"Agora o que é envelhecimento e morte? O envelhecimento dos seres nas diversas classes de seres, a sua idade avançada, os dentes quebradiços, os cabelos grisalhos, a pele enrugada, o declínio da vida, o enfraquecimento das faculdades - a isto se denomina envelhecimento. O falecimento dos seres nas várias classes de seres, a sua morte, a dissolução, o desaparecimento, o morrer, a finalização do tempo, a dissolução dos agregados, o cadáver descartado - a isto se denomina morte. Portanto esse envelhecimento e essa morte juntos são chamados envelhecimento e morte. Com o surgimento do nascimento surge o envelhecimento e morte. Com a cessação do nascimento cessa o envelhecimento e morte. Este nobre caminho óctuplo é o caminho que conduz à cessação do envelhecimento e morte; isto é, entendimento correto ... concentração correta. </p>


<p class=Normal>"Quando, bhikkhus, um nobre discípulo assim compreende o envelhecimento e morte, a sua origem, a sua cessação, e o caminho que conduz à sua cessação, esse é o seu conhecimento do princípio. Por meio desse princípio que é visto, realizado, compreendido, examinado a fundo, ele aplica o método ao passado e ao futuro assim: 'Todos contemplativos e brâmanes que no passado obtiveram o conhecimento direto do envelhecimento e morte, da sua origem, da sua cessação, e do caminho que conduz à sua cessação, todos eles obtiveram o conhecimento direto tal como eu obtive. 'Todos contemplativos e brâmanes que no futuro obterão o conhecimento direto do envelhecimento e morte, da sua origem, da sua cessação, e do caminho que conduz à sua cessação, todos eles obterão o conhecimento direto tal como eu obtive. Esse é o conhecimento resultante (do princípio). </p>


<p class=Normal>"Quando, bhikkhus, um nobre discípulo purificou esses dois tipos de conhecimento - conhecimento do princípio e conhecimento resultante - ele é chamado um nobre discípulo com o conhecimento consumado, com a visão consumada, que conquistou o verdadeiro Dhamma, que vê o verdadeiro Dhamma, que possui o conhecimento de um treinando, o conhecimento verdadeiro de um treinando, que entrou na correnteza do Dhamma, um nobre com sabedoria penetrante, que está às portas do imortal. </p>


<p class=Normal>"O que, bhikkhus, é nascimento?  ... (igual o <a href="SNXII.2.php">SN XII.2</a> ... "O que são formações volitivas?) ... Este nobre caminho óctuplo é o caminho que conduz à cessação das formações volitivas; isto é, entendimento correto ... concentração correta. </p>


<p class=Normal>"Quando, bhikkhus, um nobre discípulo assim compreende as formações volitivas, a sua origem, a sua cessação, e o caminho que conduz à sua cessação, esse é o seu conhecimento do princípio. Por meio desse princípio que é visto, realizado, compreendido, examinado a fundo, ele aplica o método ao passado e ao futuro assim: 'Todos contemplativos e brâmanes que no passado obtiveram o conhecimento direto das formações volitivas, da sua origem, da sua cessação, e do caminho que conduz à sua cessação, todos eles obtiveram o conhecimento direto tal como eu obtive. 'Todos contemplativos e brâmanes que no futuro obterão o conhecimento direto das formações volitivas, da sua origem, da sua cessação, e do caminho que conduz à sua cessação, todos eles obterão o conhecimento direto tal como eu obtive. Esse é o conhecimento resultante (do princípio). </p>


<p class=Normal>"Quando, bhikkhus, um nobre discípulo purificou esses dois tipos de conhecimento - conhecimento do princípio e conhecimento resultante - ele é chamado um nobre discípulo com o conhecimento consumado, com a visão consumada, que conquistou o verdadeiro Dhamma, que vê o verdadeiro Dhamma, que possui o conhecimento de um treinando, o conhecimento verdadeiro de um treinando, que entrou na correnteza do Dhamma, um nobre com sabedoria penetrante, que está às portas do imortal."
