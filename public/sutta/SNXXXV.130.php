<p class=Normal> Assim ouvi. Em certa ocasião, o venerável Mahakaccana estava em Avanti no Monte Papata em Kuranaghara. Então o chefe de família Haliddakani foi até o venerável Mahakaccana e depois de cumprimentá-lo sentou a um lado e disse:</p>

<p class=Normal> “Venerável senhor, foi dito pelo Abençoado: 'É na dependência da diversidade de elementos que surge a diversidade de contatos; na dependência da diversidade de contatos que surge a diversidade de sensações.' Como é isso, venerável senhor?"</p>

<p class=Normal>"Aqui, chefe de família, tendo visto uma forma com o olho, um bhikkhu compreende uma forma agradável assim: 'Assim é!' Há a consciência no olho e na dependência do contato para ser experimentado como prazeroso surge uma sensação prazerosa. Depois, tendo visto uma forma com o olho, um bhikkhu compreende uma forma desagradável assim: 'Assim é!' Há a consciência no olho e na dependência do contato para ser experimentado como desprazeroso surge uma sensação desprazerosa. Depois, tendo visto uma forma com o olho, um bhikkhu compreende uma forma que é a base para a equanimidade assim: 'Assim é!' Há a consciência no olho e na dependência do contato para ser experimentado como nem prazeroso nem desprazeroso surge uma sensação nem prazerosa nem desprazerosa.</p>

<p class=Normal>"Além disso, chefe de família, tendo ouvido um som com o ouvido ... tendo conscientizado um objeto mental com a mente, um bhikkhu compreende um objeto mental agradável assim ... um objeto mental desagradável assim ... um objeto mental que é a base para a equanimidade assim: 'Assim é!' Há a consciência no olho e na dependência do contato para ser experimentado como nem prazeroso nem desprazeroso surge uma sensação nem prazerosa nem desprazerosa.</p>

<p class=Normal>"Chefe de família, é desse modo que na dependência da diversidade de elementos surge a diversidade de contatos e na dependência da diversidade de contatos surge a diversidade de sensações." </p>

