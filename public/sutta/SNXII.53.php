<p class=Normal> Em Savatthi. "Bhikkhus, quando alguém permanece contemplando a gratificação nas coisas que agrilhoam, o desejo aumenta. Com o desejo como condição, o apego surge; com o apego como condição, o ser/existir surge; com o ser/existir como condição, o nascimento surge; com o nascimento como condição, o envelhecimento e morte, lamentação, dor, angústia e desespero surgem. Essa é a origem de toda essa massa de sofrimento. </p>

<p class=Normal> "Suponham, bhikkhus, uma lamparina queimando na dependência do azeite e do pavio e que alguém de tempos em tempos adicione azeite e regule o pavio. Assim, sustentada pelo azeite, tendo o azeite como combustível, aquela lamparina irá arder por muito tempo. Do mesmo modo, quando alguém permanece contemplando a gratificação nas coisas que agrilhoam, o desejo aumenta ... Essa é a origem de toda essa massa de sofrimento. </p>

<p class=Normal> "Bhikkhus, quando alguém permanece contemplando o perigo nas coisas que agrilhoam, o desejo cessa. Com a cessação do desejo, o apego cessa; com a cessação do apego, o ser/existir cessa; ; com a cessação do ser/existir, o nascimento cessa; com a cessação do nascimento, o envelhecimento e morte, lamentação, dor, angústia e desespero cessam. Essa é a cessação de toda essa massa de sofrimento. </p>

<p class=Normal> "Suponham, bhikkhus, uma lamparina queimando na dependência do azeite e do pavio e que alguém de tempos em tempos não adicione azeite e não regule o pavio. Assim, quando o suprimento de azeite for esgotado, sem ser alimentada com mais combustível, sem sustentação, aquela lamparina seria extinta. Do mesmo modo, quando alguém permanece contemplando o perigo nas coisas que agrilhoam, o desejo cessa ... Essa é a cessação de toda essa massa de sofrimento." </p>

