<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião o Abençoado estava em Rajagaha, no Bambual, no Santuário
dos Esquilos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. Agora,
naquela ocasião o venerável Sariputta estava perambulando pelas Colinas do Sul
com uma grande Sangha de bhikkhus. Então, um certo bhikkhu que havia passado o
retiro das chuvas em Rajagaha foi até o venerável Sariputta nas Colinas do Sul
e ambos se cumprimentaram. Quando a conversa cortês e amigável havia terminado,
ele sentou a um lado e o venerável Sariputta perguntou: “O Abençoado está bem e
forte, amigo?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O
Abençoado está bem e forte, amigo.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“A Sangha
dos bhikkhus está bem e forte, amigo?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“A Sangha
dos bhikkhus também está bem e forte, amigo.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigo, há
um brâmane chamado Dhananjani vivendo em Rajagaha no Portão de Tandulapala. O brâmane
Dhananjani está bem e forte?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Esse
brâmane Dhananjani também está bem e forte, amigo.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele é
diligente amigo?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Como
poderia ele ser diligente amigo? Ele pilha os brâmanes chefes de família em
nome do rei e ele pilha o rei em nome dos brâmanes chefes de família. A sua
esposa, que tinha fé, proveniente de um clã que tem fé, morreu e ele tomou
outra esposa, uma mulher sem fé e que vem de um clã que não possui fé.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Essas são
más notícias que estamos ouvindo, amigo. São de fato más notícias ouvir que o
brâmane Dhananjani se tornou negligente. Talvez em algum momento possamos nos
encontrar com o brâmane Dhananjani e ter com ele uma conversa.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. Então,
tendo permanecido nas Colinas do Sul pelo tempo desejado, o venerável Sariputta
saiu perambulando em direção a Rajagaha. Caminhando em etapas ele por fim
chegou em Rajagaha, estabelecendo-se no Bambual, no Santuário dos Esquilos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. Então,
ao amanhecer, o venerável Sariputta se vestiu e tomando a tigela e o manto
externo, foi para Rajagaha para esmolar alimentos. Agora, naquela ocasião o
brâmane Dhananjani estava ordenhando as suas vacas num estábulo fora da cidade.
Assim, depois do venerável Sariputta haver esmolado em Rajagaha e de haver
retornado, após a refeição ele foi até o brâmane Dhananjani. O brâmane
Dhananjani viu o venerável Sariputta chegando à distância e foi até ele
dizendo: “Beba um pouco deste leite fresco, Mestre Sariputta, até que chegue a
hora da refeição.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Já basta,
brâmane, eu já terminei a minha refeição por hoje. Eu irei até a sombra daquela
árvore para passar o resto do dia. Você poderá ir ter comigo lá.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
senhor,” ele respondeu.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. E então,
depois da sua refeição matinal, o brâmane Dhananjani foi até o venerável
Sariputta e ambos se cumprimentaram. Quando a conversa cortês e amigável havia
terminado, ele sentou a um lado e o venerável Sariputta perguntou: “Você é
diligente, Dhananjani?”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Como
podemos ser diligentes, Mestre Sariputta, quando temos que sustentar nossos
pais, nossa esposa e filhos e os nossos escravos, criados e trabalhadores;
quando temos que cumprir com os nossos deveres para com os amigos e
companheiros, para com os nossos pares e parentes, para como os nossos
hóspedes, para com os nossos antepassados que já se foram, para com as
divindades e para com o rei; e quando este corpo também precisa ser revigorado
e alimentado?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. “O que
você pensa, Dhananjani? Suponha que alguém aqui se comportasse de forma
contrária ao Dhamma, se comportasse de forma iníqua para benefício dos seus
pais, e então, devido a esse comportamento os guardiões do inferno o
arrastassem para o inferno. Seria ele capaz de se livrar defendendo-se desta
forma: ‘Foi em benefício dos meus pais que me comportei de forma contrária ao
Dhamma, que me comportei de forma iníqua, portanto, não permita que os
guardiões do inferno me arrastem para o inferno’? Ou os pais dele seriam
capazes de livrá-lo defendendo-o desta forma: ‘Foi em nosso benefício que ele
se comportou de forma contrária ao Dhamma, que ele se comportou de forma
iníqua, portanto, não permita que os guardiões do inferno o arrastem para o
inferno’?”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
Mestre Sariputta. Mesmo enquanto ele estivesse clamando, os guardiões do
inferno o arremessariam no inferno.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7-15. “O
que você pensa, Dhananjani? Suponha que alguém aqui se comportasse de forma
contrária ao Dhamma, se comportasse de forma iníqua para benefício da sua
esposa e filhos ... para benefício dos seus escravos, criados e trabalhadores
... para benefício dos seus amigos e companheiros ... para benefício dos seus
pares e parentes ... para benefício dos seus hóspedes ... para benefício dos
seus antepassados que já se foram ... para benefício dos devas ... para
benefício do rei ... para revigorar e alimentar o corpo, e então, devido a esse
comportamento os guardiões do inferno o arrastassem para o inferno. Seria ele
capaz de se livrar defendendo-se desta forma: ‘Foi para revigorar e alimentar
este corpo que me comportei de forma contrária ao Dhamma, que me comportei de
forma iníqua, portanto, não permita que os guardiões do inferno me arrastem
para o inferno’? Ou os outros seriam capazes de livrá-lo defendendo-o desta
forma: ‘Foi para revigorar e alimentar o corpo que ele se comportou de forma
contrária ao Dhamma, que ele se comportou de forma iníqua, portanto, não
permita que os guardiões do inferno o arrastem para o inferno’?”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
Mestre Sariputta. Mesmo enquanto ele estivesse clamando, os guardiões do
inferno o arremessariam no inferno.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16. “O que
você pensa, Dhananjani? Quem é o melhor, aquele que para benefício dos seus
pais se comporta de forma contrária ao Dhamma, se comporta de forma iníqua, ou
aquele que para benefício dos seus pais se comporta de acordo com o Dhamma, se
comporta com equidade?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mestre
Sariputta, aquele que para benefício dos seus pais se comporta de forma
contrária ao Dhamma, se comporta de forma iníqua, não é o melhor; aquele que
para benefício dos seus pais se comporta de acordo com o Dhamma, se comporta com
equidade, é o melhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Dhananjani,
existem outros tipos de ocupações, lucrativas e de acordo com o Dhamma, através
das quais é possível sustentar os pais e ao mesmo tempo evitar fazer o mal e
praticar o mérito.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17-25. “O
que você pensa, Dhananjani? Quem é o melhor, aquele que para benefício da sua esposa
e filhos ... para benefício dos seus escravos, criados e trabalhadores ... para
benefício dos seus amigos e companheiros ... para benefício dos seus pares e
parentes ... para benefício dos seus hóspedes ... para benefício dos seus
antepassados que já se foram ... para benefício dos devas ... para
benefício do rei ... para revigorar e alimentar o corpo se comporta de forma
contrária ao Dhamma, se comporta de forma iníqua, ou aquele que para revigorar
e alimentar o corpo se comporta de acordo com o Dhamma, se comporta com equidade?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mestre
Sariputta, aquele que para revigorar e alimentar o corpo se comporta de forma
contrária ao Dhamma, se comporta de forma iníqua, não é o melhor; aquele que
para revigorar e alimentar o corpo se comporta de acordo com o Dhamma, se
comporta com equidade, é o melhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Dhananjani,
existem outros tipos de ocupações, lucrativas e de acordo com o Dhamma, através
das quais é possível revigorar e alimentar o corpo e ao mesmo tempo evitar
fazer o mal e praticar o mérito.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>26. Então o
brâmane Dhananjani, tendo ficado satisfeito e contente com as palavras do
venerável Sariputta, levantou-se do seu assento e partiu.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>27. Mais
tarde, numa outra ocasião, o brâmane Dhananjani estava atormentado, sofrendo e
gravemente enfermo. Então, ele se dirigiu a um homem desta forma: “Venha, bom
homem, vá até o Abençoado, homenageie-o em meu nome com a sua cabeça aos pés
dele e diga: ‘Venerável senhor, o brâmane Dhananjani está atormentado, sofrendo
e gravemente enfermo; ele homenageia o Abençoado com a cabeça aos seus pés.’
Depois vá até o Venerável Sariputta, homenageie-o em meu nome com a sua cabeça
aos pés dele e diga: ‘Venerável senhor, o brâmane Dhananjani está atormentado,
sofrendo e gravemente enfermo; ele homenageia o Venerável Sariputta com a
cabeça aos seus pés.’ Então diga: ‘Seria bom, venerável senhor, se o Venerável
Sariputta fosse até a residência do brâmane Dhananjani, por compaixão.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim
senhor,” o homem respondeu, e foi até onde estava o Abençoado e após
homenageá-lo sentou a um lado e relatou a sua mensagem. Depois ele foi até o
Venerável Sariputta e após homenageá-lo relatou a sua mensagem, dizendo: “Seria
bom, venerável senhor, se o Venerável Sariputta fosse até a residência do
brâmane Dhananjani, por compaixão.” O Venerável Sariputta concordou em
silêncio.<span style='color:red'><o:p></o:p></span></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>28. Então o
Venerável Sariputta se vestiu e tomando a tigela e o manto externo, foi para a
residência do brâmane Dhananjani. Tendo chegado, ele sentou num assento
que havia sido preparado e disse para o brâmane Dhananjani: “Eu espero que você
esteja melhorando, brâmane, espero que você esteja confortável, espero que as
suas sensações de dor estejam diminuindo e não aumentando e que a sua
diminuição, não o seu aumento, seja evidente.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>29. “Mestre
Sariputta, eu não estou melhorando, não me sinto confortável. Minhas sensações
de dor estão aumentando, não diminuindo, o seu aumento, não a sua diminuição é
evidente. Tal como se um homem forte estivesse partindo a minha cabeça com uma
espada afiada, da mesma forma, ventos violentos atravessam a minha cabeça. Eu
não estou melhorando … Tal como se um homem forte estivesse apertando uma
correia de couro dura em volta da minha cabeça, da mesma forma, sinto dores
violentas na minha cabeça. Eu não estou melhorando … Tal como se um açougueiro
habilidoso ou seu aprendiz cortassem o ventre de um boi com uma faca de
açougueiro afiada, da mesma forma, ventos violentos estão cortando o meu ventre. Eu
não estou melhorando … Tal como se dois homens fortes agarrassem um homem mais
fraco por ambos os braços e o assassem sobre uma cova com carvão em brasa, da
mesma forma, um fogo violento queima no meu corpo. Eu não estou melhorando, não
me sinto confortável. Minhas sensações de dor estão aumentando, não diminuindo,
o seu aumento, não a sua diminuição é evidente.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>30. “O que
você pensa, Dhananjani? Qual é o melhor – o inferno ou o mundo animal?” – “O mundo
animal, Mestre Sariputta.”<span style="mso-spacerun: yes">  </span>“Qual é
melhor – o mundo animal ou o mundo dos fantasmas?” – “O mundo dos fantasmas,
Mestre Sariputta.”<span style="mso-spacerun: yes">  </span>“Qual é melhor – o mundo
dos fantasmas ou o mundo dos seres humanos?” – “O mundo dos seres humanos, Mestre
Sariputta.”<span style="mso-spacerun: yes">  </span>“Qual é melhor – o mundo
dos seres humanos ou o mundo dos devas dos Quatro Grandes Reis?” –
“O mundo dos devas dos Quatro Grandes Reis, Mestre Sariputta.”
“Qual é melhor – o mundo dos devas dos Quatro Grandes Reis ou o mundo
dos devas do Trinta e Três?” – “O mundo dos devas do
Trinta e três, Mestre Sariputta.” “Qual é melhor – o mundo dos devas do Trinta e Três ou o mundo dos devas de Yama?” – “O mundo dos devas
de Yama, Mestre Sariputta.” “ Qual é melhor – o mundo dos devas de Yama ou o mundo
dos devas de Tusita?” – “O mundo dos devas de Tusita,
Mestre Sariputta.” “Qual é melhor – o mundo dos devas de Tusita ou
o mundo dos devas que se deliciam com a criação?” – “O mundo dos devas que se
deliciam com a criação, Mestre Sariputta.” “Qual é melhor – o mundo dos devas
que se deliciam com a criação ou o mundo dos devas que exercem poder sobre a
criação dos outros?” – “O mundo dos devas que exercem o poder sobre a criação
dos outros, Mestre Sariputta.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>31. “O que
você pensa, Dhananjani? Qual é o melhor – o mundo dos devas que exercem o
poder sobre a criação dos outros ou o mundo de Brahma?” – “O Mestre Sariputta
disse ‘o mundo de Brahma.’ O Mestre Sariputta disse ‘o mundo de Brahma.’” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o
venerável Sariputta pensou: “Esses brâmanes são devotos do mundo de Brahma. E
se eu mostrasse ao brâmane Dhananjani o caminho para o mundo de Brahma?” [E
ele disse:] “Dhananjani, eu mostrarei o caminho para o mundo de Brahma.
Ouça e preste muita atenção àquilo que eu vou dizer.” – “Sim, senhor,” ele<span
style="mso-spacerun: yes">  </span>respondeu. O venerável Sariputta disse o
seguinte:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>32. “Qual é
o caminho para o mundo de Brahma? Aqui, Dhananjani, um bhikkhu permanece com o coração pleno de amor bondade,
permeando o primeiro quadrante com a mente imbuída de amor bondade, da mesma
forma o segundo, da mesma forma o terceiro, da mesma forma o quarto; assim,
acima, abaixo, em volta e em todos os lugares, para todos bem como para si
mesmo, ele permanece permeando o mundo todo com a mente imbuída de amor
bondade, abundante, transcendente, imensurável, sem hostilidade e sem má
vontade. Esse é o caminho para o mundo de Brahma.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>33-35.
“Outra vez, Dhananjani, um bhikkhu permanece com o coração pleno de compaixão, permeando o primeiro quadrante com
a mente imbuída de compaixão ... com a mente imbuída de alegria altruísta … com
a mente imbuída de equanimidade, da mesma forma o segundo, da mesma forma o
terceiro, da mesma forma o quarto; assim, acima, abaixo, em volta e em todos os
lugares, para todos bem como para si mesmo, ele permanece permeando o mundo
todo com a mente imbuída de equanimidade, abundante, transcendente,
imensurável, sem hostilidade e sem má vontade. Esse também é o caminho para o mundo de Brahma.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>36. “Então
Mestre Sariputta, homenageie o Abençoado em meu nome com a sua cabeça aos pés
dele e diga: ‘Venerável senhor, o brâmane Dhananjani estava atormentado,
sofrendo e gravemente enfermo; ele homenageia o Abençoado com a cabeça aos seus
pés.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o
venerável Sariputta, tendo estabelecido o brâmane Dhananjani no mundo de
Brahma, levantou-se do seu assento e partiu, havendo no entanto ainda mais por
ser feito.[<a href="#N1">1</a>] <a name=R1></a>Pouco depois que o venerável
Sariputta partiu o brâmane Dhananjani morreu e renasceu no mundo de Brahma.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>37. Então o
Abençoado se dirigiu aos bhikkhus da seguinte forma: “Bhikkhus, Sariputta,
tendo estabelecido o brâmane Dhananjani no mundo de Brahma, levantou-se
do seu assento e partiu, havendo no entanto ainda mais por ser feito.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>38. Então o
venerável Sariputta foi até o Abençoado e depois de homenageá-lo sentou a um
lado e disse: “Venerável senhor,<span style="mso-spacerun: yes">  </span>o
brâmane Dhananjani estava atormentado, sofrendo e gravemente enfermo; ele
homenageia o Abençoado com a cabeça aos seus pés.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sariputta,
tendo estabelecido o brâmane Dhananjani no mundo de Brahma, porque
você se levantou do seu assento e partiu, quando no entanto havia ainda mais
por ser feito?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, eu pensei o seguinte: ‘Esses brâmanes são devotos do mundo de Brahma. E
se eu mostrasse ao brâmane Dhananjani o caminho para o mundo de Brahma.’”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sariputta,
o brâmane Dhananjani morreu e renasceu no mundo de Brahma.” [<a href="#N2">2</a>]<o:p></o:p></span></p>


