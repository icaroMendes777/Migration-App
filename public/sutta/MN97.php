<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[1] <i>Sati uttarakaraniye.</i> O ven.
Sariputta partiu sem dar ao brâmane Dhananjani um ensinamento que lhe
