<p class=Normal> Em certa ocasião, o Abençoado estava perambulando por  Kosala com uma
grande sangha de bhikkhus até que por fim ele chegou em Kesaputta, uma cidade dos
Kalamas. Os Kalamas de Kesaputta ouviram dizer, “Gotama o contemplativo, o
filho dos Sakyas, que adotou a vida santa deixando o clã dos Sakyas, que andava
perambulando em Kosala com um grande número de bhikkhus chegou em Kesaputta. E
acerca desse mestre Gotama existe essa boa reputação: ‘Esse Abençoado é um arahant, perfeitamente iluminado,
consumado no verdadeiro conhecimento e conduta, bem-aventurado, conhecedor dos
mundos, um líder insuperável de pessoas preparadas para serem treinadas, mestre
de devas e humanos, desperto, sublime. Ele declara - tendo realizado por si
próprio com o conhecimento direto - este mundo com os seus devas, maras e
brahmas, esta população com seus contemplativos e brâmanes, seus príncipes e 
povo. Ele ensina o Dhamma, com o significado e fraseado corretos, que é
admirável no início, admirável no meio, admirável no final; e ele revela uma
vida santa que é completamente perfeita e imaculada. É bom poder encontrar
alguém tão nobre.”<o:p></o:p></span></p>





<p class=Normal>Assim os Kalamas de Kesaputta foram até o Abençoado. Alguns homenagearam o Abençoado e sentaram a um lado; alguns trocaram saudações corteses com ele e após a troca de saudações sentaram a um lado; alguns ajuntaram as mãos em respeitosa saudação e sentaram a um lado; alguns anunciaram o seu nome e clã e sentaram a um lado. Alguns permaneceram em silêncio e sentaram a um lado.<o:p></o:p></span></p>





<p class=Normal>Uma vez sentados, eles disseram para o Abençoado: “Venerável senhor,
existem alguns brâmanes e contemplativos que vêm para Kesaputta. Eles explicam
e glorificam as suas doutrinas, porém com relação às doutrinas de outros, eles
as desaprovam, as repelem, demonstram desprezo por elas e fazem pouco delas. E
então outros brâmanes e contemplativos vêm para Kesaputta. Eles explicam e
glorificam as suas doutrinas, porém com relação às doutrinas de outros, eles as
desaprovam, as repelem, demonstram desprezo por elas e fazem pouco delas. Eles
nos deixam confusos e em dúvida: quais desses brâmanes e contemplativos estão
falando a verdade e quais estão mentindo?”<o:p></o:p></span></p>





<p class=Normal>“Claro que vocês estão confusos Kalamas. Claro que vocês têm dúvidas.
Porque a dúvida surge com relação a qualquer assunto que cause a perplexidade.
Dessa forma, Kalamas, nesse caso não se deixem levar pelos relatos, pelas
tradições, pelos rumores, por aquilo que está nas escrituras, pela razão, pela inferência, pela analogia, pela competência (ou confiabilidade) de alguém, por respeito por alguém, ou pelo pensamento, ‘Este contemplativo
é o nosso mestre.’ Quando vocês souberem por vocês mesmos que, ‘Essas qualidades
são inábeis; essas qualidades são culpáveis; essas qualidades são passíveis de
crítica pelos sábios; essas qualidades quando postas em prática conduzem ao mal
e ao sofrimento’ - então vocês devem abandoná-las.<o:p></o:p></span></p>





<p class=Normal>“O que vocês pensam Kalamas? Quando a cobiça surge em uma pessoa, ela
surge para o bem ou para o mal?”<o:p></o:p></span></p>





<p class=Normal>“Para o mal, venerável senhor.”<o:p></o:p></span></p>





<p class=Normal>“E essa pessoa que está tomada pela cobiça, sua mente obcecada com a
cobiça, mata seres vivos, toma o que não lhe é dado, busca a mulher do próximo,
mente, e induz outros a fazerem o mesmo, tudo o que resulta é dano e sofrimento
por um longo tempo.”<o:p></o:p></span></p>





<p class=Normal>“Sim, venerável senhor”<o:p></o:p></span></p>





<p class=Normal>“E o que vocês pensam, Kalamas? Quando a raiva surge em uma pessoa,
ela surge para o bem ou para o mal?”<o:p></o:p></span></p>





<p class=Normal>“Para o mal, venerável senhor.”<o:p></o:p></span></p>





<p class=Normal>“E essa pessoa tomada pela raiva, sua mente obcecada pela raiva,
mata seres vivos, toma o que não lhe é dado, busca a mulher do próximo, mente,
e induz outros a fazerem o mesmo, tudo o que resulta é dano e sofrimento por um
longo tempo.”<o:p></o:p></span></p>





<p class=Normal>“Sim, venerável senhor.”<o:p></o:p></span></p>





<p class=Normal>“E o que vocês pensam, Kalamas? Quando a delusão surge em uma pessoa,
ela surge para o bem ou para o mal?”<o:p></o:p></span></p>





<p class=Normal>“Para o mal, venerável senhor.”<o:p></o:p></span></p>





<p class=Normal>“E essa pessoa tomada pela delusão, sua mente obcecada pela delusão,
mata seres vivos, toma o que não lhe é dado, busca a mulher do próximo, mente,
e induz outros a fazerem o mesmo, tudo o que resulta é dano e sofrimento por um
longo tempo.”<o:p></o:p></span></p>





<p class=Normal>“Sim, venerável senhor.”<o:p></o:p></span></p>





<p class=Normal>“Então Kalamas o que vocês pensam. Essa qualidades são hábeis ou
inábeis?”<o:p></o:p></span></p>





<p class=Normal>“Inábeis, venerável senhor.”<o:p></o:p></span></p>





<p class=Normal>“Culpáveis ou isentas de culpa?”<o:p></o:p></span></p>





<p class=Normal>“Culpáveis, venerável senhor.”<o:p></o:p></span></p>





<p class=Normal>“Criticadas pelos sábios ou elogiadas pelos sábios?”<o:p></o:p></span></p>





<p class=Normal>“Criticadas pelos sábios, venerável senhor.”<o:p></o:p></span></p>





<p class=Normal>“Quando postas em prática elas conduzem ao mal e ao sofrimento ou não?”<o:p></o:p></span></p>





<p class=Normal>“Quando postas em prática elas conduzem ao mal e ao sofrimento. Assim é
como as vemos.”<o:p></o:p></span></p>





<p class=Normal>“Então como eu disse, Kalamas: ‘Não se deixem levar pelos relatos,
pelas tradições, pelos rumores, por aquilo que está nas escrituras, pela razão, pela inferência, pela analogia, pela competência (ou confiabilidade) de alguém, por respeito por alguém, ou pelo pensamento, “Este contemplativo
é o nosso mestre.” Quando vocês souberem por vocês mesmos que, “Essas qualidades
são inábeis; essas qualidades são culpáveis; essas qualidades são criticáveis
pelos sábios; essas qualidades quando postas em prática conduzem ao mal e ao
sofrimento” - então vocês devem abandoná-las,’ foi por causa disso que isso foi dito.<o:p></o:p></span></p>





<p class=Normal>“Agora Kalamas, não se deixem levar pelos relatos, pelas tradições,
pelos rumores, por aquilo que está nas escrituras, pela razão, pela
inferência, pela analogia, pela competência (ou confiabilidade) de alguém, por respeito por alguém, ou pelo pensamento, ‘Este contemplativo é o nosso
mestre.’<span style='mso-spacerun:yes'>  </span>Quando vocês souberem por vocês
mesmos que, ‘Essas qualidades são hábeis; essas qualidades são isentas de
culpa; essas qualidades são elogiadas pelos sábios; essas qualidades quando
postas em prática conduzem ao bem-estar e à felicidade' - então vocês devem
penetrar e permanecer nelas.<o:p></o:p></span></p>





<p class=Normal>“O que vocês pensam Kalamas? Quando a ausência de cobiça surge em uma
pessoa, ela surge para o bem ou para o mal?”<o:p></o:p></span></p>





<p class=Normal>“Para o bem, venerável senhor.”<o:p></o:p></span></p>





<p class=Normal>“E essa pessoa que não está tomada pela cobiça, sua mente não está
obcecada pela cobiça, não<span style='mso-spacerun:yes'>  </span>mata seres
vivos, não toma o que não lhe é dado, não busca a mulher do próximo, não mente,
e não induz outros a fazerem o mesmo, tudo o que resulta para o bem-estar e
felicidade por um longo tempo.”<o:p></o:p></span></p>





<p class=Normal>“Sim, venerável senhor”<o:p></o:p></span></p>





<p class=Normal>“O que vocês pensam Kalamas? Quando a ausência de raiva surge em uma
pessoa, ela surge para o bem ou para o mal?”<o:p></o:p></span></p>





<p class=Normal>“Para o bem, venerável senhor.”<o:p></o:p></span></p>





<p class=Normal>“E essa pessoa que não está tomada pela raiva, sua mente não está
obcecada pela raiva, não<span style='mso-spacerun:yes'>  </span>mata seres
vivos, não toma o que não lhe é dado, não busca a mulher do próximo, não mente,
e não induz outros a fazerem o mesmo, tudo o que resulta para o bem-estar e
felicidade por um longo tempo.”<o:p></o:p></span></p>





<p class=Normal>“Sim, venerável senhor”<o:p></o:p></span></p>





<p class=Normal>“O que vocês pensam Kalamas? Quando a ausência de delusão surge em uma
pessoa, ela surge para o bem ou para o mal?”<o:p></o:p></span></p>





<p class=Normal>“Para o bem, venerável senhor.”<o:p></o:p></span></p>





<p class=Normal>“E essa pessoa que não está tomada pela delusão, sua mente não está
obcecada pela delusão, não<span style='mso-spacerun:yes'>  </span>mata seres
vivos, não toma o que não lhe é dado, não busca a mulher do próximo, não mente,
e não induz outros a fazerem o mesmo, tudo o que resulta para o bem-estar e
felicidade por um longo tempo.”<o:p></o:p></span></p>





<p class=Normal>“Sim, venerável senhor”<o:p></o:p></span></p>





<p class=Normal>“Então Kalamas o que vocês pensam. Essa qualidades são hábeis ou
inábeis?”<o:p></o:p></span></p>





<p class=Normal>“Hábeis, venerável senhor.”<o:p></o:p></span></p>





<p class=Normal>“Culpáveis ou isentas de culpa?”<o:p></o:p></span></p>





<p class=Normal>“Isentas de culpa, venerável senhor.”<o:p></o:p></span></p>





<p class=Normal>“Criticadas pelos sábios ou elogiadas pelos sábios?”<o:p></o:p></span></p>





<p class=Normal>“Elogiadas pelos sábios, venerável senhor.”<o:p></o:p></span></p>





<p class=Normal>“Quando postas em prática elas conduzem ao bem e à felicidade ou não?”<o:p></o:p></span></p>





<p class=Normal>“Quando postas em prática elas conduzem ao bem e à felicidade. Assim é
como as vemos.”<o:p></o:p></span></p>





<p class=Normal>“Então como eu disse, Kalamas: ‘Não se deixem levar pelos relatos,
pelas tradições, pelos rumores, por aquilo que está nas escrituras, pela razão, pela inferência, pela analogia, pela competência (ou confiabilidade) de alguém, por respeito por alguém, ou pelo pensamento, “Este contemplativo
é o nosso mestre.”<span style='mso-spacerun:yes'>  </span>Quando vocês souberem
por vocês mesmos que, “Essas qualidades são hábeis; essas qualidades são
isentas de culpa; essas qualidades são elogiadas pelos sábios; essas qualidades
quando postas em prática conduzem ao bem e à felicidade” - então vocês devem
entrar e permanecer nelas.’ Assim foi dito. E em referência a isso é que foi
dito.<o:p></o:p></span></p>





<p class=Normal>“Agora Kalamas, aquele que é um nobre discípulo - portanto despojado de
cobiça, despojado de má vontade, não deludido, com atenção plena e plena
consciência – permanece com o coração pleno de amor bondade, permeando
o primeiro quadrante com a mente imbuída de amor bondade, da mesma forma o
segundo, da mesma forma o terceiro, da mesma forma o quarto; assim acima,
abaixo, em volta e em todos os lugares, para todos bem como para si mesmo, ele
permeia o mundo todo com a mente imbuída de amor bondade, abundante,
transcendente, imensurável, sem hostilidade e sem má vontade .<o:p></o:p></span></p>





<p class=Normal>“Ele permanece com o coração pleno de compaixão, permeando
o primeiro quadrante com a mente imbuída de compaixão, da mesma forma o
segundo, da mesma forma o terceiro, da mesma forma o quarto; assim acima,
abaixo, em volta e em todos os lugares, para todos bem como para si mesmo, ele
permeia o mundo todo com a mente imbuída de compaixão, abundante,
transcendente, imensurável, sem hostilidade e sem má vontade.<o:p></o:p></span></p>





<p class=Normal>“Ele permanece com o coração pleno de alegria altruísta,
permeando o primeiro quadrante com a mente imbuída de alegria altruísta, da
mesma forma o segundo, da mesma forma o terceiro, da mesma forma o quarto;
assim acima, abaixo, em volta e em todos os lugares, para todos bem como para
si mesmo, ele permeia o mundo todo com a mente imbuída de alegria altruísta,
abundante, transcendente, imensurável, sem hostilidade e sem má vontade.<o:p></o:p></span></p>





<p class=Normal>“Ele permanece com o coração pleno de equanimidade,
permeando o primeiro quadrante com a mente imbuída de equanimidade, da mesma
forma o segundo, da mesma forma o terceiro, da mesma forma o quarto; assim
acima, abaixo, em volta e em todos os lugares, para todos bem como para si
mesmo, ele permeia o mundo todo com a mente imbuída de equanimidade, abundante,
transcendente, imensurável, sem hostilidade e sem má vontade .<o:p></o:p></span></p>





<p class=Normal>“Agora Kalamas, aquele que é um nobre discípulo - sua mente livre de
hostilidades, livre de má vontade, imaculada e pura - obtém quatro garantias no
aqui e agora:<o:p></o:p></span></p>





<p class=Normal>“’Se existe um mundo após a morte, se existem conseqüências das boas e
más ações, então essa é a base pela qual com a dissolução do corpo, após a
morte, renascerei num destino feliz, no paraíso.’ Essa é a primeira garantia
que ele obtém.<o:p></o:p></span></p>





<p class=Normal>“’Porém se não existe um mundo após a morte, se não existem
conseqüências das boas e más ações, então nesta vida cuidarei de mim mesmo com
tranqüilidade - livre de hostilidades, livre de má vontade, livre de dificuldades.”
Esta é a segunda garantia que ele obtém.<o:p></o:p></span></p>





<p class=Normal>“’Se conseqüências ruins recaem sobre quem pratica ações más, eu no
entanto não penso em agir com maldade, então, como poderão os resultados ruins
recaírem sobre mim?” Essa é a terceira garantia que ele obtém.<o:p></o:p></span></p>





<p class=Normal>“’Porém se conseqüências ruins não recaírem sobre quem pratica ações
más, então de todas formas estou purificado.” Essa é a quarta garantia que ele
obtém.<o:p></o:p></span></p>





<p class=Normal>“Aquele que é um nobre discípulo - sua mente livre de hostilidades,
livre de má vontade, imaculada e pura - obtém essas quatro garantias no aqui e
agora.”<o:p></o:p></span></p>





<p class=Normal>“Assim é, Abençoado. O nobre discípulo - sua mente livre de
hostilidades, livre de má vontade, imaculada e pura - obtém quatro garantias no
aqui e agora: <o:p></o:p></span></p>





<p class=Normal><span style='mso-spacerun:yes'> </span>“’Se existe um mundo após a
morte, se existem conseqüências das boas e más ações, então essa é a base pela
qual com a dissolução do corpo, após a morte, renascerei num destino feliz,
no paraíso.’ Essa é a primeira garantia que ele obtém.<o:p></o:p></span></p>





<p class=Normal>“’Porém se não existe um mundo após a morte, se não existem
conseqüências das boas e más ações, então nesta vida cuidarei de mim mesmo com
tranqüilidade - livre de hostilidades, livre de má vontade, livre de
dificuldades.” Esta é a segunda garantia que ele obtém.<o:p></o:p></span></p>





<p class=Normal>“’Se conseqüências ruins recaem sobre quem pratica ações más, eu no
