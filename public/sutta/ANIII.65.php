<p class=Normal>“Aquele que é um nobre discípulo - sua mente livre de hostilidades,
livre de má vontade, imaculada e pura - obtém essas quatro garantias no aqui e
agora.”<o:p></o:p></span></p>




<p class=Normal> “Magnífico, Mestre
Gotama! Magnífico, Mestre Gotama! Mestre Gotama esclareceu o Dhamma de várias
formas, como se tivesse colocado em pé o que estava de cabeça para baixo,
revelasse o que estava escondido, mostrasse o caminho para alguém que estivesse
perdido ou segurasse uma lâmpada no escuro para aqueles que possuem visão
pudessem ver as formas. Nós buscamos refúgio no Abençoado, no Dhamma e na
Sangha dos Bhikkhus. Que o Abençoado se recorde de nós como os discípulos
leigos que  nele buscaram
refúgio para o resto da vida.”<o:p></o:p></span></p>


