<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. Então, ao amanhecer, a bhikkhuni Vajira se vestiu e tomando a tigela
e o manto externo foi para Savatthi esmolar alimentos. Depois de haver esmolado
alimentos em Savatthi e de haver retornado, após a refeição, ela se dirigiu ao
bosque dos Homens Cegos em busca de isolamento. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, Mara, o Senhor do
Mal, desejando despertar medo, trepidação e terror na bhikkhuni Vajira,
desejando que ela abandonasse a sua concentração, se aproximou e se dirigiu a
ela em versos: <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Por quem foi criado esse ser?<br>
Onde está o criador desse ser?<br>
Onde surgiu esse ser?<br>
Onde termina esse ser?”<span style="mso-spacerun: yes">  </span><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Foi então que a bhikkhuni
Vajira pensou: “Agora, quem será que recitou esses versos – um ser humano ou um
ser não humano?” Então lhe ocorreu: “Foi Mara, o Senhor do Mal, quem recitou
esses versos desejando despertar medo, trepidação e terror em mim, desejando
que eu abandone a minha concentração.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então a bhikkhuni Vajira,
tendo compreendido, “Este é Mara, o Senhor do Mal,” respondeu em versos: <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
 “Porque você pressupõe um ‘ser’?<br>
Mara, essa idéia é sua conjectura?<br>
Este é um amontoado de puras fabricações:<br>
aqui nenhum ser será encontrado.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR style='mso-ansi-language:PT-BR'>Tal qual uma montagem de
partes,<br>
a palavra ‘carruagem’ é empregada,<br>
assim, quando os agregados estão presentes,<br>
existe a convenção ‘um ser.’ <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR style='mso-ansi-language:PT-BR'>É só sofrimento que
surge,<br>
o sofrimento que permanece e cessa.<br>
Nada além do sofrimento surge,<br>
nada além do sofrimento cessa.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, Mara, o Senhor do
Mal, compreendendo que, “A bhikkhuni Vajira sabe quem sou,” triste e desapontado,
desapareceu no mesmo instante. <o:p></o:p></span></p>


