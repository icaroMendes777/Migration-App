Como a sua mente não está invadida pela cobiça, ele não estaria sujeito à morte
ou ao sofrimento igual à morte.<o:p></o:p></span></p>



<p class=Normal><a name=T27></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>27. “Sunakkhatta, eu citei esse símile de forma a transmitir uma idéia.
A idéia é a seguinte: ‘Ferida’ é um termo para as seis bases internas, ‘Humor
venenoso’ é um termo para ignorância. ‘Flecha’ é um termo para o desejo.
‘Instrumento’ é um termo para a atenção plena. ‘Faca’ é um termo para a nobre
sabedoria. ‘Cirurgião’ é um termo para o Tathagata, </span>um arahant,
perfeitamente iluminado<span lang=PT-BR style='mso-ansi-language:PT-BR'>.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>28.
“Quando, Sunakkhatta, um bhikkhu pratica a contenção nas seis bases de contato,
tendo compreendido que as aquisições são a raiz do sofrimento, [</span><a
href="#N10"><span lang=PT-BR style='mso-ansi-language:PT-BR'>10</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R10></a>se livra das aquisições
e é libertado através da destruição das aquisições, não é possível que ele
dirija o seu corpo ou estimule a sua mente para quaisquer aquisições.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>29.
“Suponha, Sunakkhatta, que houvesse uma taça de bronze com uma bebida com boa
cor, cheiro e sabor, mas estivesse misturada com veneno, e viesse um homem<span
style="mso-spacerun: yes">  </span>que deseja viver, não morrer, que deseja o
prazer e evita a dor. [</span><a href="#N11"><span lang=PT-BR style='mso-ansi-language:
PT-BR'>11</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>] <a
name=R12></a><a name=R11></a>O que você pensa, Sunakkhatta, aquele homem
beberia aquela taça de bebida, sabendo: ‘Se eu beber isto estarei sujeito à
morte ou ao sofrimento igual à morte’?” – “Não, venerável senhor.” – “Da mesma
forma, quando um bhikkhu pratica a contenção nas seis bases de contato,<span
style="mso-spacerun: yes">  </span>tendo compreendido que as aquisições são a
raiz do sofrimento, se livra das aquisições e é libertado através da destruição
das aquisições, não é possível que ele dirija o seu corpo ou estimule a sua
mente para quaisquer aquisições.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>30.
“Suponha, Sunakkhatta, que houvesse uma cobra venenosa mortífera e viesse um
homem que deseja viver, não morrer, que deseja o prazer e evita a dor. O que
você pensa, Sunakkhatta, aquele homem daria a sua mão ou o dedo para a cobra
venenosa mortífera, sabendo: ‘Se eu for picado por ela estarei sujeito à morte
ou ao sofrimento igual à morte’?” – “Não, venerável senhor.” – “Da mesma forma,
quando um bhikkhu pratica a contenção nas seis bases de contato, tendo
compreendido que as aquisições são a raiz do sofrimento, se livra das aquisições
e é libertado através da destruição das aquisições, não é possível que ele
dirija o seu corpo ou estimule a sua mente para quaisquer aquisições.”<o:p></o:p></span></p>



<p class=Normal><a name=R31></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Isso foi o que disse o Abençoado. Sunakkhatta, filho dos Licchavis,
ficou satisfeito e contente com as palavras do Abençoado.”<o:p></o:p></span></p>


