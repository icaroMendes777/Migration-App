irá ouví-la ou dar-lhe atenção ou esforçar a mente para compreendê-la. Ela não
se associa com esse tipo de pessoa e não encontra satisfação através disso.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9.
“Suponha, Sunakkhatta, que um homem tenha deixado seu vilarejo ou cidade há
