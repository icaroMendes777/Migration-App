senhor.” – “Da mesma forma, Sunakkhatta, é possível que uma pessoa tenha
empenho por coisas materiais mundanas. Quando uma pessoa tem empenho por coisas
materiais mundanas ... e não encontra satisfação através disso. Ela deve ser
compreendida como uma pessoa que tem empenho por coisas materiais mundanas.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. “É
possível, Sunakkhatta, que uma pessoa se empenhe pelo imperturbável. [</span><a
