<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isto foi
dito pelo Abençoado, dito pelo Arahant, assim ouvi: “Bhikkhus, não temam ações meritórias.
Essa é uma outra forma de descrever aquilo que é ditoso, desejável, prazeroso,
afetuoso, encantador – isto é, ações meritórias. Tenho conhecimento disso
através da minha própria experiência, pois tendo durante muito tempo realizado
ações meritórias, durante muito tempo experimentei resultados ditosos,
desejáveis, prazerosos, afetuosos, encantadores. Tendo desenvolvido uma mente
com amor bondade durante sete anos, então durante sete éons de contração e
expansão eu não retornei a este mundo. Sempre que um éon contraía, eu ia para o
mundo de Abhassara Brahma. Sempre que o éon expandia eu reaparecia no palácio
de Brahma vazio. E lá eu era Brahma, o Grande Brahma, o Conquistador, o
Não-conquistado, Onisciente, Todo Poderoso. [<a href="#N1">1</a>] <a name=R1></a>Depois
por trinta e seis vezes eu fui Sakka, o senhor dos devas. Por muitas centenas
de vezes eu fui um rei, um monarca que gira a roda, um monarca justo que
governou de acordo com o Dhamma; conquistador dos quatro pontos cardeais, que
estabeleceu a segurança no seu reino e que possuía os sete tesouros. [<a
href="#N2">2</a>] <a name=R2></a>Sem falar nas vezes em que fui um rei local. O
pensamento me ocorreu: ‘De qual ação minha será este o fruto, de qual ação será
este o resultado, que eu agora tenha tamanho poder e força?’ Então o pensamento
me ocorreu: ‘Isto é o fruto dos meus três tipos de ações, o resultado de três
tipos de ações, que eu agora tenha tamanho poder e força: isto é, generosidade,
autocontrole e contenção.’”<o:p></o:p></span></p>


