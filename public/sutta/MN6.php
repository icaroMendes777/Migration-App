<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. </span>Assim
ouvi. Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no
Parque de Anathapindika<span lang=PT-BR style='mso-ansi-language:PT-BR'>. </span>Lá
ele se dirigiu aos monges desta forma: “Bhikkhus” – “Venerável Senhor,” eles
responderam. O Abençoado disse o seguinte<span lang=PT-BR style='mso-ansi-language:
PT-BR'>:<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.
“Bhikkhus, permaneçam possuídos pela virtude, possuídos pelo Patimokkha, contidos
pelas regras do Patimokkha, sejam perfeitos na conduta e na sua esfera de
atividades, temendo a menor falha, treinem adotando os preceitos de virtude.[<a href="#N1">1</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>3. “Se um bhikkhu desejar: ‘Que eu seja querido e agradável para os meus
companheiros na vida santa, respeitado e benquisto por eles,’ que ele cumpra os
preceitos, seja dedicado à tranqüilidade da mente, não negligencie a meditação que conduz aos jhanas,
cultive o insight e habite cabanas vazias.[<a href="#N2">2</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>4. “Se um bhikkhu desejar: ‘Que eu obtenha mantos, comida esmolada,
moradia e medicamentos,’ que ele cumpra os preceitos...<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. “Se um
bhikkhu desejar: ‘Que as oferendas daqueles cujos mantos, comida esmolada,
moradia e medicamentos eu uso, lhes traga grandes frutos e benefícios,’ que ele
cumpra os preceitos...<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. “Se um
bhikkhu desejar: ‘Quando os meus pares e parentes, que faleceram, se recordarem
de mim com confiança nas suas mentes, que isso lhes traga grandes frutos e
grandes benefícios,’ que ele cumpra os preceitos ... [<a href="#N3">3</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>7. “Se um bhikkhu desejar: ‘Que eu me torne um conquistador do
descontentamento e do deleite, e que o descontentamento e o deleite não me
conquistem; que eu permaneça transcendendo o descontentamento e o deleite
sempre que estes surgirem,’ que ele cumpra os preceitos...<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. “Se um
bhikkhu desejar: ‘Que eu me torne um conquistador do medo e do terror,[<a href="#N3a">3a</a>] <a name=R3a></a>e que o
medo e o terror não me conquistem; que eu permaneça transcendendo o medo e o
terror sempre que estes surgirem,’que ele cumpra os preceitos...<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. “Se um
bhikkhu desejar: ‘Que eu me torne um daqueles que obtém de acordo com a
vontade, sem problemas ou dificuldades, os quatro jhanas que constituem a mente
superior e que proporcionam uma estada prazerosa aqui e agora,’ que ele cumpra
os preceitos... .<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. “Se um
bhikkhu desejar: ‘Que eu toque com o corpo, e permaneça, naquelas libertações que
são pacíficas e imateriais que transcendem as formas,’ que ele cumpra os
preceitos... [<a href="#N4">4</a>]<o:p></o:p></span></p>


<p class=Normal><a name=R4></a><a name=T11></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>11. “Se um bhikkhu desejar: ‘Que eu, através da
destruição dos três grilhões, me torne um que entrou na correnteza, </span>não
mais destinado aos mundos inferiores, com o destino fixo, tendo a iluminação
como destino<span lang=PT-BR style='mso-ansi-language:PT-BR'>,’ que ele cumpra
os preceitos... [<a href="#N5">5</a>]<o:p></o:p></span></p>


<p class=Normal><a name=R5></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>12. “Se um bhikkhu desejar: ‘Que eu, através da destruição dos três
grilhões e com a atenuação da cobiça, raiva e delusão, me torne um que retorna
apenas uma vez, retornando a este mundo apenas uma vez para dar um fim ao
sofrimento,’ que ele cumpra os preceitos...<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. “Se um
bhikkhu desejar: ‘Que eu, através da destruição dos cinco primeiros grilhões,
renasça espontaneamente [nas Moradas Puras] e lá realize o parinibbana, sem
nunca mais retornar daquele mundo,’ que ele cumpra os preceitos ... [<a
href="#N6">6</a>]<o:p></o:p></span></p>


<p class=Normal><a name=R6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>14. “Se um bhikkhu desejar:[<a href="#N7">7</a>] <a name=R7></a>‘Que eu
possa exercer os vários tipos de poderes supra-humanos: sendo um, que eu me
torne vários; sendo vários, que eu me torne um; que eu apareça e desapareça;
que eu cruze uma parede sem nenhum problema, cruze um cercado, uma montanha,
como se cruzasse o espaço; que eu mergulhe e saia da terra como se fosse água;
caminhe sobre a água sem afundar, como se fosse terra; sentado de pernas
cruzadas, que eu cruze o espaço como se fosse um pássaro; com a minha mão toque
e acaricie a lua e o sol tão forte e poderoso; que eu exerça poderes corporais
até mesmo nos distantes mundos de Brahma,' que ele cumpra os preceitos...<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. “Se um
bhikkhu desejar: ‘Que eu, com o elemento do ouvido divino, que é purificado e
sobrepuja o humano, ouça tanto os sons divinos como os humanos, aqueles que
estão distantes bem como os que estão próximos,’ que ele cumpra os preceitos...<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16. “Se um
bhikkhu desejar: ‘Que eu compreenda as mentes de outros seres, de outras
pessoas, abarcando-as com a minha própria mente. Que eu compreenda uma mente
afetada pelo desejo como afetada pelo desejo e uma mente não afetada pelo
desejo como não afetada pelo desejo; Que eu compreenda uma mente afetada pela
raiva como afetada pela raiva e uma mente não afetada pela raiva como não
afetada pela raiva; Que eu compreenda uma mente afetada pela delusão como
afetada pela delusão e uma mente não afetada pela delusão como não afetada pela
delusão; Que eu compreenda uma mente contraída como contraída e uma mente
distraída como distraída; Que eu compreenda uma mente transcendente como
transcendente e uma mente não transcendente como não transcendente; Que eu
compreenda uma mente superável como superável e uma mente não superável como
não superável; Que eu compreenda uma mente concentrada como concentrada e uma
mente não concentrada como não concentrada; Que eu compreenda uma mente
libertada como libertada e uma mente não libertada como não libertada,' que ele
cumpra os preceitos...<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17. “Se um
bhikkhu desejar: ‘Que eu me recorde das minhas muitas vidas passadas, isto é,
um nascimento, dois nascimentos ... (igual ao <a href="MN4.php#T27">MN 4.27</a>)
... Que assim eu me recorde das minhas muitas vidas passadas nos seus modos e
detalhes,’ que ele cumpra os preceitos...<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18. “Se um
bhikkhu desejar: ‘Que eu, por meio do olho divino, que é purificado e sobrepuja
o humano, veja seres falecendo e renascendo, inferiores e superiores, bonitos e
feios, afortunados e desafortunados. Que eu compreenda como os seres prosseguem
de acordo com as suas ações desta forma: ... (igual ao <a href="MN4.php#T29">MN
4.29</a>) ... que ele cumpra os preceitos...<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>19. “Se um
bhikkhu desejar: ‘Que eu, compreendendo por mim mesmo com conhecimento direto,
aqui e agora, entre e permaneça na libertação da mente e na libertação através
da sabedoria, que são imaculadas, com a destruição de todas as impurezas,’ [<a
href="#N8">8</a>] <a name=R8></a>que ele cumpra os preceitos, seja dedicado à
tranqüilidade da mente, não negligencie a meditação que conduz aos jhanas, cultive o insight
 e habite
cabanas vazias.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>20.
“Portanto, foi com referência a isso que foi dito: ‘Bhikkhus, permaneçam
possuídos pela virtude, possuídos pelo Patimokkha, </span>contidos pelas regras
do Patimokkha, sejam perfeitos na conduta e na sua esfera de atividades,
temendo a menor falha, treinem adotando os preceitos de virtude<span
lang=PT-BR style='mso-ansi-language:PT-BR'>.’”<o:p></o:p></span></p>


<p class=Normal>Isso foi o que disse o Abençoado. Os bhikkhus ficaram
satisfeitos e contentes com as palavras do Abençoado<span lang=PT-BR
style='mso-ansi-language:PT-BR'>.<o:p></o:p></span></p>


