<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[1] MA diz que a expressão <i>sampannasila</i>,
traduzida aqui como “possuído pela virtude,” pode significar tanto “perfeito em
virtude”, (<i>paripunnasila</i>), como “dotado de virtude”, (<i>silasamangino</i>).
O Patimokkha é o código de disciplina monástica que na versão em Pali consiste
de 227 regras. “Esfera de Atividades”, (<i>gocara</i>), implica um local
apropriado para esmolar alimentos, embora também possa significar o
comportamento apropriado de um monge, a sua postura serena e controlada. Os
termos deste trecho são analisados no Vsm I, 43-52. [<a href="#R1">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[2] MA: A frase que começa com “</span><span
lang=PT-BR style='mso-ansi-language:PT-BR'>que ele cumpra os preceitos ...”
repetida em cada uma das seções seguintes até o final do sutta, compreende o
treinamento tríplice completo. O trecho sobre cumprir os preceitos significa o
treinamento na virtude superior,<span style='mso-bidi-font-weight:bold'> (<i>adhisilasikkha</i>);
o trecho,“</span>seja dedicado à tranqüilidade da mente, não negligencie a meditação que conduz aos jhanas” 
indica o treinamento em concentração ou mente superior, <span
style='mso-bidi-font-weight:bold'>(<i>adhicittasikkha</i>); e o trecho “</span>cultive
o insight” aponta para o treinamento na sabedoria superior,<span
style='mso-bidi-font-weight:bold'> (<i>adhipaññasikkha</i>). O trecho “</span>habite
cabanas vazias” combina os últimos dois treinamentos, visto que uma pessoa
busca a cabana vazia para desenvolver a tranqüilidade e o insight<span
style='mso-bidi-font-weight:bold'>. [<a href="#R2">Retorna</a>]<o:p></o:p></span></span></p>


<p class=Normal><a name=N3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[3] Isto é, se os pares e parentes, que renasceram no
mundo dos fantasmas, <i> (petas)</i>, ou em algum mundo inferior dos devas, se recordarem dos
bhikkhus virtuosos com confiança, essa confiança se tornará uma fonte de mérito
para eles, protegendo-os de renascimentos ruins e tornando-se uma condição
positiva para a realização de Nibbana. [<a href="#R3">Retorna</a>]<o:p></o:p></span></p>

<p class=Normal><a name=N3a></a><span lang=PT-BR style='mso-ansi-language:
