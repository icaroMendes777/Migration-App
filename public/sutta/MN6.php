<p class=Normal><a name=N8></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[8] MA: Neste trecho, “mente” e “sabedoria”
significam, respectivamente, a concentração e a sabedoria associadas ao fruto
de arahant. A concentração é chamada de “libertação da mente”, (<i>cetovimutti</i>),
porque está libertada da cobiça; a sabedoria é chamada de “libertação através da
sabedoria”, (<i>paññavimutti</i>), porque está libertada da ignorância. A primeira
normalmente é o resultado da tranqüilidade, a última é o resultado do insight.
Mas quando elas são combinadas e descritas como imaculadas, (<i>anasava</i>),
elas unidas são o resultado da destruição das impurezas através do caminho
supramundano do arahant. [<a href="#R8">Retorna</a>]</span><b style='mso-bidi-font-weight:
normal'><span lang=PT-BR style='mso-ansi-language:PT-BR'><o:p></o:p></span></b></p>


