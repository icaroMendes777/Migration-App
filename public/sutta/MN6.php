 terror tem o sentido apenas objetivo, 
isto é a pessoa ou coisa que amedronta, aterroriza. [<a href="#R3a">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N4></a><span lang=PT-BR style='mso-ansi-language:
