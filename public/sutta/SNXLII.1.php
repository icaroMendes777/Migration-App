<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. Então, o chefe tribal Canda, o irado, foi até o Abençoado e depois de
cumprimentá-lo sentou a um lado e disse:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, qual é a causa e razão porque alguém aqui é considerado irado? E qual é
a causa e razão porque alguém aqui é considerado afável?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
chefe tribal, alguém não abandonou a cobiça. Porque ele não abandonou a cobiça,
outras pessoas o irritam. Sendo irritado pelos outros, ele manifesta irritação:
ele é considerado irado. Ele não abandonou a raiva. Porque ele não abandonou a
raiva, outras pessoas o irritam. Sendo irritado pelos outros, ele manifesta
irritação: ele é considerado irado. Ele não abandonou a delusão. Porque ele não
abandonou a delusão, outras pessoas o irritam. Sendo irritado pelos outros, ele
manifesta irritação: ele é considerado irado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Essa,
chefe tribal, é a causa e razão porque alguém aqui é considerado irado.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
chefe tribal, alguém abandonou a cobiça. Porque ele abandonou a cobiça, outras
pessoas não o irritam. Não sendo irritado pelos outros, ele não manifesta
irritação: ele é considerado afável. Ele abandonou a raiva. Porque ele
abandonou a raiva, outras pessoas não o irritam. Não sendo irritado pelos
outros, ele não manifesta irritação: ele é considerado afável. Ele abandonou a
delusão. Porque ele abandonou a delusão, outras pessoas não o irritam. Não
sendo irritado pelos outros, ele não manifesta irritação: ele é considerado
afável.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Essa,
chefe tribal, é a causa e razão porque alguém aqui é considerado afável.” <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR;mso-bidi-font-weight:
bold'>Quando isso foi dito, o chefe tribal Canda disse para o Abençoado: </span><span
lang=PT-BR style='mso-ansi-language:PT-BR'>“Magnífico, Venerável senhor!
Magnífico, Venerável senhor! O Abençoado esclareceu o Dhamma de várias formas,
como se tivesse colocado em pé o que estava de cabeça para baixo, revelasse o
que estava escondido, mostrasse o caminho para alguém que estivesse perdido ou
segurasse uma lâmpada no escuro para aqueles que possuem visão pudessem ver as
formas. Nós buscamos refúgio no Abençoado, no Dhamma e na Sangha dos bhikkhus.
Que o Abençoado nos aceite como discípulo leigo que nele buscou refúgio para o
resto da vida.”<span style='mso-bidi-font-weight:bold'><o:p></o:p></span></span></p>


