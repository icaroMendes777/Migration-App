<p class=Normal><a name=N4></a><span style='mso-bidi-font-weight:bold'>[4]
MA explica que não existem mundos distintos de devas chamados de “Radiância
Contaminada” e “Radiância Pura.” Ambos são subdivisões dentro dos dois mundos
– dos devas da Radiância Limitada e dos devas da Radiância Imensurável.
O renascimento entre os devas da Radiância Limitada é determinado pela
realização do (segundo) jhana com o sinal limitado de uma kasina e o
renascimento entre os devas da Radiância Imensurável, através da realização
do mesmo jhana, com o sinal expandido de uma kasina. O renascimento na
radiância contaminada se aplica àqueles que não obtiveram maestria nos
jhanas e não os purificaram dos estados obstrutivos; o renascimento na radiância
pura se aplica àqueles que obtiveram essa maestria e purificação. [<a href="#R4">Retorna</a>]<o:p></o:p></span></p>


