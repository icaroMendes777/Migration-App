<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Em certa
ocasião o Abençoado estava em Savatthi, no Bosque de Jeta, no Parque de
Anathapindika. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. Então o
carpinteiro Pancakanga se dirigiu a um certo homem da seguinte forma: “Venha,
bom homem, vá até o venerável Anuruddha, preste uma homenagem em meu nome com a
sua cabeça aos pés dele, e diga: ‘Venerável senhor, o carpinteiro Pancakanga
homenageia o venerável Anuruddha com a cabeça aos seus pés e diz: “Venerável
senhor, que o venerável Anuruddha junto com outros três, consinta em aceitar a
refeição de amanhã do carpinteiro Pancakanga; e que o venerável Anuruddha possa
chegar pontualmente já que o carpinteiro Pancakanga está muito ocupado e tem
muito trabalho para fazer para o rei.”’” <o:p></o:p></span></p>



<p class=Normal>“Sim, senhor,” aquele homem respondeu e foi até o venerável
Anuruddha. Depois de cumprimentar o venerável Anuruddha, ele sentou<span
style="mso-spacerun: yes">  </span>a um lado e comunicou a sua mensagem. O
venerável Anuruddha concordou em silêncio.</p>



<p class=Normal>3. Então, quando havia terminado a noite, ao amanhecer, o
venerável Anuruddha se vestiu e tomando a sua tigela e o manto
externo foi até a casa do carpinteiro Pancakanga, sentando num assento que
havia sido preparado. Então, com as próprias mãos, o carpinteiro Pancakanga
serviu e satisfez o venerável Anuruddha com os vários tipos de boa comida. Em
seguida, quando o venerável Anuruddha havia terminado de comer e retirado a mão
da sua tigela, o carpinteiro Pancakanga sentou a um lado, num assento mais
baixo, e disse para o venerável Anuruddha:</p>



<p class=Normal>4. “Veja, venerável senhor, bhikkhus sêniores vieram até mim
e disseram: ‘Chefe de família, desenvolva a libertação imensurável da mente’; e
alguns bhikkhus disseram: ‘Chefe de família, desenvolva a libertação
transcendente da mente.’ Venerável senhor, a libertação imensurável da mente e
a libertação transcendente da mente [<a href="#N1">1</a>] <a name=R1></a>–
esses estados são distintos no significado e distintos no nome, ou eles possuem
o mesmo significado e são distintos apenas no nome?”</p>



<p class=Normal>5. “Explique o modo como você entende, chefe de família.
Depois isso será aclarado para você.” </p>

<p class=Normal>“Venerável senhor, eu penso da seguinte forma: a libertação
imensurável da mente e a libertação transcendente da mente – esses estados
possuem o mesmo significado e são distintos apenas no nome.”</p>



<p class=Normal>6. “Chefe de família,<span style="mso-spacerun: yes"> 
</span>a libertação imensurável da mente e a libertação transcendente da mente
– são estados distintos no significado e distintos no nome. E deve ser
compreendido da seguinte forma como esses estados são distintos no significado
e distintos no nome.</p>



<p class=Normal>7. “O que, chefe de família, é a libertação imensurável da
mente? Aqui um bhikkhu com o coração pleno de amor bondade, permanece permeando o primeiro quadrante com a mente
imbuída de amor bondade, da mesma forma o segundo, da mesma forma o terceiro,
da mesma forma o quarto; assim acima, abaixo, em volta e em todos os lugares,
para todos bem como para si mesmo, ele permanece permeando o mundo todo com a
mente imbuída de amor bondade, abundante, transcendente, imensurável, sem
hostilidade e sem má vontade. Com o coração pleno de compaixão, ele permanece permeando o primeiro quadrante com
a mente imbuída de compaixão ... Ele permanece permeando o primeiro quadrante
com a mente imbuída de alegria altruísta … Ele permanece permeando o primeiro
quadrante com a mente imbuída de equanimidade … abundante, transcendente,
imensurável, sem hostilidade e sem má vontade. Isso é chamado de libertação
imensurável da mente.</p>



<p class=Normal>8. “E o que, chefe de família, é a libertação transcendente
da mente? Neste caso um bhikkhu permanece resoluto sobre uma área do tamanho da
raiz de uma árvore, permeando-a com a mente transcendente: isso é chamado de
libertação transcendente da mente. [<a href="#N2">2</a>] <a name=R2></a>Aqui um
bhikkhu permanece resoluto sobre uma área do tamanho da raiz de duas ou três árvores,
permeando-a com a mente transcendente: isso é chamado de libertação
transcendente da mente. Aqui um bhikkhu permanece resoluto sobre uma área do
tamanho de um vilarejo, permeando-a com a mente transcendente ... uma área do
tamanho de dois ou três vilarejos … uma área do tamanho de um reino majestoso …
uma área do tamanho de dois ou três reinos majestosos … uma área do tamanho de
um continente circundado pelo oceano, permeando-a com a mente transcendente:
isso também é chamado de libertação transcendente da mente. Desta maneira,
chefe de família, pode ser compreendido como esses estados são distintos no
significado e distintos no nome.</p>



<p class=Normal>9. “Existem, chefes de família, esses quatro tipos de
ressurgimento (num futuro estado) de ser. [<a href="#N3">3</a>] <a name=R3></a>Quais
quatro? Aqui alguém permanece resoluto e permeando a ‘radiância limitada’;
na dissolução do corpo, após a morte, ele renasce na companhia dos devas da
Radiância Limitada. Aqui alguém permanece resoluto e permeando a
‘radiância imensurável’; na dissolução do corpo, após a morte, ele renasce
na companhia dos devas da Radiância Imensurável. Aqui alguém permanece
resoluto e permeando a ‘radiância contaminada’; na dissolução do corpo, após
a morte, ele renasce na companhia dos devas da Radiância Contaminada. Aqui
alguém permanece resoluto e permeando a ‘radiância pura’; na dissolução do
corpo, após a morte, ele renasce na companhia dos devas da Radiância Pura.
Esses são os quatro tipos de ressurgimento (num futuro estado) de ser. [<a
href="#N4">4</a>]</p>



<p class=Normal><a name=R4></a>10. “Há uma ocasião, chefe de família, em que
esses devas se reúnem num lugar. Quando eles estão reunidos num lugar, uma
diferença na sua coloração pode ser discernida mas não há diferença na sua luminosidade.
Como se um homem trouxesse várias lamparinas de azeite para uma casa, uma
diferença na chama das lamparinas poderia ser discernida mas nenhuma diferença
na sua luminosidade; assim também, há uma ocasião em que esses devas se
reúnem num lugar ... mas não há diferença na sua luminosidade.</p>



<p class=Normal>11. “Há uma ocasião, chefe de família, em que esses
devas dali desaparecem. Quando eles se dispersam, uma diferença na sua
coloração pode ser discernida e também uma diferença na sua luminosidade. Como
se um homem removesse aquelas várias lamparinas de azeite da casa, uma
diferença na chama das lamparinas poderia ser discernida e também uma diferença
na sua luminosidade; assim também, há uma ocasião em que esses devas dali
desaparecem ... e também uma diferença na sua luminosidade.</p>



<p class=Normal>12. “A esses devas não lhes ocorre que: ‘Esta nossa
vida é permanente, irá durar para sempre, ou ela é eterna,’ no entanto, em
qualquer lugar em que elas se estabeleçam, elas ali encontram o deleite. Como
quando moscas são levadas numa vara ou num cesto, a elas não lhes ocorre que:
‘Esta nossa vida é permanente, irá durar para sempre, ou ela é eterna,’ no
entanto, em qualquer lugar em que aquelas moscas se estabeleçam, elas ali<span
style="mso-spacerun: yes">  </span>encontram o deleite; assim também, a esses
devas não lhes ocorre ... no entanto, em qualquer lugar em que eles se
estabeleçam, elas ali encontram o deleite.”</p>



<p class=Normal>13. Quando isso foi dito, o venerável Abhiya Kaccana disse
para o venerável Anuruddha: “Muito bem, venerável Anuruddha, eu ainda tenho
algo mais para perguntar: Todos esses seres luminosos são devas de
Radiância Limitada ou alguns são devas de Radiância Imensurável?”</p>



<p class=Normal>“Devido ao fator responsável pelo renascimento, amigo
Kaccana, alguns são devas de Radiância Limitada, alguns são
devas de Radiância Imensurável.”</p>



<p class=Normal>14. “Venerável Anuruddha, qual é a causa e razão porque
dentre esses devas que renasceram numa única ordem de devas, alguns são
devas de Radiância Limitada e alguns são devas de Radiância
Imensurável?”</p>



<p class=Normal>“Quanto a isso, amigo Kaccana, responderei com uma pergunta
em retorno. Responda como quiser. O que você pensa, amigo Kaccana? Quando um
bhikkhu permanece resoluto sobre uma área do tamanho da raiz de uma árvore,
permeando-a com a mente transcendente, e um outro bhikkhu permanece resoluto
sobre uma área do tamanho da raiz de duas ou três árvores, permeando-a com a
mente transcendente – qual desses tipos de desenvolvimento mental é mais
transcendente?” – “O segundo, venerável senhor.”</p>



<p class=Normal>“O que você pensa, amigo Kaccana? Quando um bhikkhu
permanece resoluto sobre uma área do tamanho da raiz de duas ou três árvores,
permeando-a com a mente transcendente, e um outro bhikkhu permanece resoluto
sobre uma área do tamanho de um vilarejo, permeando-a com a mente transcendente
... uma área do tamanho de dois ou três vilarejos … uma área do tamanho de um
reino majestoso … uma área do tamanho de dois ou três reinos majestoso … uma
área do tamanho de um continente circundado pelo oceano, permeando-a com a
mente transcendente - qual desses tipos de desenvolvimento mental é mais
transcendente?” – “O último, venerável senhor.”</p>



<p class=Normal>“Essa é a causa e razão, amigo Kaccana, porque dentre esses
devas que renasceram numa única ordem de devas, alguns são devas de
Radiância Limitada e alguns são devas de Radiância Imensurável.</p>



<p class=Normal>15. “Muito bem, venerável Anuruddha, eu ainda tenho algo
mais para perguntar: Todos esses seres luminosos são devas de Radiância
Contaminada ou alguns são devas de Radiância Pura?”</p>



<p class=Normal>“Devido ao fator responsável pelo renascimento, amigo
Kaccana, alguns são devas de Radiância Contaminada, alguns são
devas de Radiância Pura.”</p>



<p class=Normal>16. “Venerável Anuruddha, qual é a causa e razão porque
dentre esses devas que renasceram numa única ordem de devas, alguns são
devas de Radiância Contaminada e alguns são devas de Radiância
Pura?”</p>



<p class=Normal>“Nesse caso, amigo Kaccana, explicarei com um símile pois
alguns homens sábios compreendem o significado de um enunciado através de um
símile. Suponha que uma lamparina de azeite esteja queimando com um azeite
impuro e um pavio impuro; devido à impureza do azeite e do pavio ela arde com
debilidade. Assim também, aqui um bhikkhu permanece resoluto e permeando uma
área com uma luminosidade contaminada. A sua inércia corporal não foi
completamente apaziguada, a sua preguiça e torpor não foram completamente
eliminados, a sua inquietação e ansiedade não foram completamente removidos;
devido a isso ele medita, como se fosse, com debilidade. [<a href="#N5">5</a>] <a
name=R5></a>Na dissolução do corpo, após a morte, ele renasce na companhia dos
devas da Radiância Contaminada.</p>



<p class=Normal>Suponha que uma lamparina de azeite esteja queimando com um
azeite puro e um pavio puro; devido à pureza do azeite e do pavio ela não arde
com debilidade. Assim também, aqui um bhikkhu permanece resoluto e permeando
uma área com uma luminosidade pura. A sua inércia corporal foi completamente
apaziguada, a sua preguiça e torpor foram completamente eliminados, a sua
inquietação e ansiedade foram completamente removidos; devido a isso ele
medita, como se fosse, com luminosidade. Na dissolução do corpo, após a morte,
ele renasce na companhia dos devas da Radiância Pura.</p>



<p class=Normal>“Essa é a causa e razão, amigo Kaccana, porque dentre esses
devas que renascem numa única ordem de devas, alguns são devas de
Radiância Contaminada e alguns são devas de Radiância Pura.</p>



<p class=Normal>17. Quando isso foi dito, o venerável Abhiya Kaccana disse
para o venerável Anuruddha: “Muito bem, venerável Anuruddha. O venerável
Anuruddha não diz: ‘Assim ouvi’ ou ‘Deve ser assim.’ Ao invés disso, o
venerável Anuruddha diz: ‘Estes devas são assim e aqueles devas são assim.’
Ocorre-me, venerável senhor, que o venerável Anuruddha com certeza se associou
com esses devas, dirigiu-lhes a palavra e com eles manteve conversações.”</p>



<p class=Normal>“Certamente, amigo Kaccana, as suas palavras são ofensivas e
descorteses, mas ainda assim vou responder. Durante muito tempo me associei com
esses devas, dirigi a palavra e mantive conversações com eles.” [<a
href="#N6">6</a>]</p>



<p class=Normal><a name=R6></a>18. Quando isso foi dito, o venerável Abhiya
Kaccana disse para o carpinteiro Pancakanga: “É um ganho para você, chefe de
família, é um grande ganho para você ter abandonado o seu estado de dúvida e
ter tido a oportunidade de ouvir este discurso do Dhamma.”</p>




