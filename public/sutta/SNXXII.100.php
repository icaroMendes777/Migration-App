<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mesmo a
