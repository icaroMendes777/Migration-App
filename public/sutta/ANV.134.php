<p class=Normal> "Bhikkhus, qualquer quadrante no qual ele esteja, um rei khattiya ungido que possui cinco fatores, permanece no seu próprio reino. Quais cinco? </p>

<p class=Normal> (1) "Aqui, um rei khattiya ungido é bem nascido pelos dois lados, com a descendência pura por parte da mãe e do pai por sete gerações, inatacável e impecável com respeito ao nascimento, (2) Ele é rico, com grande riqueza e grandes posses. (3) Ele é poderoso, possuindo um exército com quatro divisões, leal e confiável. (4) O seu conselheiro é sábio, competente, e inteligente, capaz de considerar os benefícios no passado, futuro e presente. (5) Essas quatro qualidades promovem a sua fama, Possuindo essas cinco qualidades, incluindo a fama, em qualquer quadrante no qual ele esteja, ele permanece no seu próprio reino. Por qual razão? Porque assim é como ocorre com os vitoriosos. </p>

<p class=Normal> "Da mesma forma, bhikkhus, em qualquer quadrante no qual ele esteja, um bhikkhu que possui cinco fatores, permanece com a mente liberta. Quais cinco? </p>

<p class=Normal> (1) "Aqui, um bhikkhu é virtuoso; ele permanece contido pelas regras do Patimokkha, perfeito na conduta e na sua esfera de atividades. Temendo a menor falha, ele treina adotando os preceitos de virtude. Isso é igual ao rei khattiya ungido ser bem nascido. (2) Ele aprendeu muito, se recorda daquilo que aprendeu e consolida aquilo que aprendeu. Aqueles ensinamentos que são admiráveis no início, admiráveis no meio, admiráveis no final, com o correto significado e fraseado e que revelam uma vida santa que é completamente perfeita e imaculada – ensinamentos como esses ele os aprendeu bem, se recorda, domina com a linguagem, investigou com a mente e penetrou corretamente com o entendimento. Isso é igual ao rei khattiya ungido ser rico, com grande riqueza e grandes posses. (3) Ele permanece com a sua energia desperta para o abandono de qualidades mentais prejudiciais e para a obtenção de qualidades mentais benéficas; ele é decidido, firme no seu esforço, sem se esquivar das suas responsabilidades com relação ao cultivo de qualidades mentais benéficas. Isso é igual ao rei khattiya ungido ser poderoso. (4) Ele é sábio, ele possui a completa compreensão da origem e cessação, que é nobre e penetrante, conduzindo à completa destruição do sofrimento. Isso é igual ao rei khattiya ungido que possui um bom conselheiro. (5) Essas quatro qualidades promovem a sua libertação. Possuindo essas cinco qualidades, incluindo a libertação, em qualquer quadrante no qual ele esteja, ele permanece com a mente liberta. Por qual razão? Porque assim é como ocorre com a mente liberta." </p>

