<a name=R1></a>Ouçam e prestem muita atenção àquilo que eu vou dizer.” – “Sim,
venerável senhor,” os bhikkhus responderam. O Abençoado disse o seguinte: <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é o caminho que faz parte da penetração? Os sete fatores da
iluminação. Quais sete? O fator da iluminação da atenção plena ... o fator da
iluminação da equanimidade.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isso
foi dito, o Venerável Udayi perguntou ao Abençoado: “Venerável
senhor, como os sete fatores da iluminação são desenvolvidos e cultivados para
que conduzam à penetração?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Nesse
caso, Udayi, um bhikkhu desenvolve o fator da iluminação da atenção plena, que
tem como base o afastamento, desapego e cessação, que amadurece no abandono;
que é vasto, glorificado, imensurável, sem má vontade. Com uma mente que tenha
desenvolvido o fator da iluminação da atenção plena, ele penetra e parte a
massa de cobiça que ele nunca antes havia penetrado e partido; ele penetra e
parte a massa de raiva que ele nunca antes havia penetrado e partido; ele
penetra e parte a massa de delusão que ele nunca antes havia penetrado e
partido.<o:p></o:p></span></p>

<p class=Normal>...</p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele
desenvolve o fator da iluminação da equanimidade, que tem como base o afastamento,
desapego e cessação, que amadurece no abandono; que é vasto, glorificado,
imensurável, sem má vontade. Com uma mente que tenha desenvolvido o fator da
iluminação da equanimidade, ele penetra e parte a massa de cobiça ... a massa
de raiva ... a massa de delusão que ele nunca antes havia penetrado e partido.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando,
Udayi, os sete fatores da iluminação são desenvolvidos e cultivados dessa forma
é que eles conduzem à penetração.”<o:p></o:p></span></p>


