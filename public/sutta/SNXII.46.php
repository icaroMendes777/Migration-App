<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em Savathi.
Então, um certo brâmane foi até o Abençoado e ambos se cumprimentaram. Quando a
conversa cortês e amigável havia terminado ele sentou a um lado e disse para o
Abençoado: <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Como é
isso, Mestre Gotama: aquele que age é o mesmo que experimenta (os resultados da
ação)?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(O Buda:)
“(Dizer,) ‘Aquele que age é o mesmo que experimenta,’ é um extremo.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(O brâmane:)
“Então, Mestre Gotama, aquele que age é alguém diferente daquele que
experimenta?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(O Buda:)
“(Dizer,) ‘Aquele que age é alguém diferente daquele que experimenta,’ é um
outro extremo. Evitando esses dois extremos, o Tathagata ensina o Dhamma pelo
meio: Da ignorância como condição, as formações volitivas [surgem]. Das formações volitivas como
condição, a consciência. Da consciência como condição, a
mentalidade-materialidade  (nome e forma). Da mentalidade-materialidade  (nome e forma) como condição, as seis
bases dos sentidos. Das seis bases dos sentidos como condição, o contato. Do
contato como condição, a sensação. Da sensação como condição, o desejo. Do
desejo como condição, o apego. Do apego como condição, o ser/existir. Do
ser/existir como condição, o nascimento. Do nascimento como condição, então o
envelhecimento e morte, tristeza, lamentação, dor, angústia e desespero surgem.
Essa é a origem de toda essa massa de sofrimento.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Agora, do
desaparecimento e cessação sem deixar vestígios dessa mesma ignorância cessam
as formações volitivas. Da cessação das formações volitivas, cessa a consciência. Da cessação da
consciência, cessa a mentalidade-materialidade  (nome e forma). Da cessação da
mentalidade-materialidade  (nome e forma), cessam as seis bases dos sentidos. Da cessação das
seis bases dos sentidos, cessa o contato. Da cessação do contato, cessa a
sensação. Da cessação da sensação, cessa o desejo. Da cessação do desejo, cessa
o apego. Da cessação do apego, cessa o ser/existir. Da cessação do ser/existir,
cessa o nascimento. Da cessação do nascimento, então o envelhecimento e morte,
tristeza, lamentação, dor, angústia e desespero, tudo cessa. Essa é a cessação
de toda essa massa de sofrimento.“<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isso
foi dito, o brâmane disse para o Abençoado: “Magnífico, Mestre Gotama!
Magnífico, Mestre Gotama! Mestre Gotama esclareceu o Dhamma de várias formas,
como se tivesse colocado em pé o que estava de cabeça para baixo, revelasse o
que estava escondido, mostrasse o caminho para alguém que estivesse perdido ou
segurasse uma lâmpada no escuro para aqueles que possuem visão pudessem ver as
formas. Eu busco refúgio no Mestre Gotama, no Dhamma e na Sangha dos bhikkhus.
Que o Mestre Gotama se recorde de mim como um discípulo leigo que nele buscou
refúgio deste dia em diante, pelo resto da sua vida.”<a name=N6></a><a
name=N18></a><o:p></o:p></span></p>


