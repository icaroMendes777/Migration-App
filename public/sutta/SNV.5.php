<p class=Normal> Em
Savatthi. Então, ao amanhecer, a bhikkhuni Uppalavanna se vestiu e tomando a
tigela e o manto externo foi para Savatthi esmolar alimentos. Depois de haver
esmolado alimentos em Savatthi e de haver retornado, após a refeição, ela se
dirigiu ao bosque dos Homens Cegos, ao pé de uma árvore Sala em plena
florescência, em busca de isolamento. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então Mara, o Senhor do
Mal, desejando despertar medo, trepidação e terror na bhikkhuni Uppalavanna,
desejando que ela abandonasse a sua concentração, se aproximou e se dirigiu a
ela em versos: <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
 “Tendo se dirigido a uma árvore <i>sala</i> em plena florescência, <br>
você permanece ao pé dela completamente só, bhikkhuni.<br>
Não há ninguém que possa rivalizar a sua beleza: <br>
garota tola, você não teme os patifes?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Foi então que a bhikkhuni
Uppalavanna pensou: “Agora, quem será que recitou esses versos – um ser humano ou
um ser não humano?” Então lhe ocorreu: “Foi Mara, o Senhor do Mal, quem recitou
esses versos desejando despertar medo, trepidação e terror em mim, desejando
que eu abandone a minha concentração.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, a bhikkhuni
Uppalavanna, tendo compreendido, “Este é Mara, o Senhor do Mal,” respondeu em
versos: <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
 “Embora cem mil patifes<br>
iguais a você possam vir até aqui, <br>
eu não movo nem um fio de cabelo, eu não sinto medo;<br>
mesmo só, Mara, eu não o temo. <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
Posso desaparecer
<br>
ou posso penetrar dentro da sua barriga.<br>
Posso me colocar entre as suas sobrancelhas<br>
e apesar disso, você não me veria nem de relance. <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
Eu sou senhora da minha
mente,<br>
as bases do poder estão bem desenvolvidas;<br>
eu estou liberta de todo tipo de grilhão,<br>
por conseguinte, não o temo, amigo.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, Mara, o Senhor do
Mal, compreendendo que, “A bhikkhuni Uppalavanna sabe quem sou,” triste e
desapontado, desapareceu no mesmo instante. <o:p></o:p></span></p>



