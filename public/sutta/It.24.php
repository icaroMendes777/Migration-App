<p class=Normal> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: Bhikkhus, os ossos de uma única pessoa perambulando neste samsara durante um éon fariam uma pilha, uma quantidade de ossos tão grande quanto este Monte Vepulla, se houvesse alguém para coletá-los e se o coletado não fosse destruído."</p>

<p class=MsoNormal style='margin-left:.75in'> Os ossos de uma única pessoa <br>
acumulados durante um éon<br>
fariam uma pilha igual a uma montanha -<br>
assim disse o Grande Sábio.</p>

<p class=MsoNormal style='margin-left:.75in'> Ele declarou que seria<br>
tão grande quanto o Monte Vepulla<br>
ao norte do Pico do Abutre<br>
no forte na colina de Magadha.</p>

<p class=MsoNormal style='margin-left:.75in'> Mas quando alguém vê com perfeita sabedoria<br>
as quatro nobres verdades tal como são -<br>
sofrimento, a origem do sofrimento, <br>
a cessação do sofrimento, <br>
e o nobre caminho óctuplo<br>
que conduz ao alívio do sofrimento -</p>

<p class=MsoNormal style='margin-left:.75in'> tendo apenas perambulado<br>
no máximo sete vezes, <br>
com a destruição dos grilhões<br>
ele dá um fim ao sofrimento. [<a href="#N1">1</a>]


