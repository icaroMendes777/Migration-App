<p class=Normal> Em Savatthi. Então, o Venerável Migajala foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse: “Venerável senhor, seria bom se o Abençoado pudesse me ensinar o Dhamma de maneira resumida, de modo que, tendo ouvido o Dhamma do Abençoado, eu possa permanecer só, isolado, diligente, ardente e decidido.”</p>

<p class=Normal> “Migajala, há formas conscientizadas através do olho que são desejáveis, agradáveis e fáceis de serem gostadas, conectadas com o desejo sensual e que provocam a cobiça, e se um bhikkhu nelas busca o prazer, acolhe-as e permanece atado a elas, o deleite surge. Com o surgimento do deleite, eu digo, Migajala, ocorre o surgimento do sofrimento.</p>

<p class=Normal> “Há sons conscientizados através do ouvido … aromas conscientizados através do nariz … sabores conscientizados através da língua … tangíveis conscientizados através do corpo ... objetos mentais conscientizados através da mente que são desejáveis, agradáveis e fáceis de serem gostados, conectados com o desejo sensual e que provocam a cobiça e se um bhikkhu neles busca o prazer, acolhe-os e permanece atado a eles, o deleite surge. Com o surgimento do deleite, eu digo, Migajala, ocorre o surgimento do sofrimento.</p>

<p class=Normal> “Há, Migajala, formas conscientizadas através do olho e que são desejáveis, agradáveis e fáceis de serem gostadas, conectadas com o desejo sensual e que provocam a cobiça, e se um bhikkhu não busca nelas o prazer, não as acolhe e não permanece atado a elas, o deleite cessa. Com a cessação do deleite, eu digo, Migajala, ocorre a cessação do sofrimento.</p>

<p class=Normal> “Há, Migajala, sons conscientizados através do ouvido … aromas conscientizados através do nariz … sabores conscientizados através da língua … tangíveis conscientizados através do corpo ... objetos mentais conscientizados através da mente que são desejáveis, agradáveis e fáceis de serem gostados, conectados com o desejo sensual e que provocam a cobiça, e se um bhikkhu não busca neles o prazer, não os acolhe e não permanece atado a eles, o deleite cessa. Com a cessação do deleite, eu digo, Migajala, ocorre a cessação do sofrimento."</p>

<p class=Normal> Então, o Venerável Migajala, tendo ficado satisfeito e contente com as palavras do Abençoado, levantou-se do seu assento e depois de homenageá-lo, mantendo-o à sua direita, partiu. Permanecendo só, isolado, diligente, ardente e decidido, em pouco tempo, o Venerável Migajala alcançou e permaneceu no objetivo supremo da vida santa pelo qual membros de um clã deixam a vida em família pela vida santa, tendo conhecido e realizado por si mesmo no aqui e agora. Ele soube: “O nascimento foi destruído, a vida santa foi vivida, o que deveria ser feito foi feito, não há mais vir a ser a nenhum estado.” E assim aquele bhikkhu tornou-se mais um dos Arahants.</p>

