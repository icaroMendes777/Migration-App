<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus, certa
vez um falcão mergulhou subitamente sobre uma codorna e a agarrou. Então, a
codorna, enquanto estava sendo levada pelo falcão, lamentou ‘Oh, que má sorte e
falta de mérito, eu estar vagueando fora do meu domínio e no campo dos outros!
Se eu tivesse permanecido hoje no meu domínio, no meu campo ancestral, esse
falcão não se igualaria a mim em batalha.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’Mas qual é o seu
domínio?’ perguntou o falcão. ‘Qual é o seu campo ancestral?’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’Um campo recém arado com
a terra toda remexida.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Assim o falcão, confiando
na sua própria força, sem alardear a sua própria força, soltou a codorna
dizendo: ‘Vá, codorna, mas mesmo estando lá você não me escapará.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então, bhikkhus, a
codorna, foi até um campo recém-arado, com a terra toda remexida e subindo
sobre um torrão de terra, ficou zombando do falcão: ‘Venha agora e me pegue,
seu falcão! Venha agora e me pegue, seu falcão! ‘<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Assim, o falcão confiando
na sua própria força, sem alardear a sua própria força, dobrou as asas e
subitamente mergulhou em direção à codorna. Mas quando a codorna se deu conta,
‘O falcão está vindo na minha direção a toda velocidade,’ ela se escondeu atrás
de um torrão de terra e ali mesmo o falcão esmagou o seu próprio peito.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Isso é o que acontece,
bhikkhus, com qualquer um que vagueie fora do seu domínio, no campo dos outros.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto, bhikkhus, não
vagueiem fora do seu domínio, no campo dos outros. Mara ganhará acesso àqueles
que vagueiam fora do seu domínio, no campo dos outros. Mara irá controlá-los. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual não é, para um
bhikkhu, o seu domínio mas é o campo dos outros? Os cinco elementos do prazer sensual. Quais cinco? As formas percebidas pelo olho que são desejáveis,
agradáveis e fáceis de serem gostadas, conectadas com o desejo sensual e que
provocam a cobiça. Sons percebidos pelo ouvido … Aromas percebidos pelo nariz …
Sabores percebidos pela língua …Tangíveis percebidos pelo corpo que são
desejáveis, agradáveis e fáceis de serem gostados, conectados com o desejo
sensual e que provocam a cobiça. Isso, para um bhikkhu, não é o seu domínio,
mas o campo dos outros.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Movam-se, bhikkhus, dentro
do seu domínio, do seu campo ancestral. Mara não terá acesso àqueles que se
movem dentro do seu domínio, do seu campo ancestral, Mara não irá controlá-los.
E qual é, para um bhikkhu, o seu domínio, o seu campo ancestral? Os quatro
fundamentos da atenção plena. Quais quatro? Aqui, bhikkhus, um bhikkhu
permanece contemplando o corpo como um corpo, ardente, plenamente consciente e
com atenção plena, tendo colocado de lado a cobiça e o desprazer pelo mundo.
Ele permanece contemplando as sensações como sensações ... a mente como mente
... os objetos mentais como objetos mentais, ardente, plenamente consciente e
com atenção plena, tendo colocado de lado a cobiça e o desprazer pelo mundo.
Isto é, para um bhikkhu, o seu domínio, o seu campo ancestral.”</span><a
