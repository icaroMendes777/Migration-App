<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim ouvi.
Em certa ocasião o Abençoado estava perambulando por Kosala com uma grande
sangha de bhikkhus até que por fim acabou chegando no vilarejo brâmane
denominado Nagaravinda.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. Os
brâmanes chefes de família de Nagaravinda ouviram: “Gotama o contemplativo, o
filho dos Sakyas, que adotou a vida santa deixando o clã dos Sakyas, que andava
perambulando em Kosala com um grande número de bhikkhus chegou em Nagaravinda.
E acerca desse mestre Gotama existe essa boa reputação: ‘Esse Abençoado é
um arahant, perfeitamente iluminado ... ( Igual ao <a href="MN41.php#T2">MN 41,
verso 2</a>) ... e ele revela uma vida santa que é completamente perfeita e
imaculada.’ É bom poder encontrar alguém tão nobre.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. Assim os
brâmanes chefes de família de Nagaravinda foram até o Abençoado. Alguns
homenagearam o Abençoado e sentaram a um lado; alguns trocaram saudações
corteses com ele e após a troca de saudações sentaram a um lado; alguns
ajuntaram as mãos em respeitosa saudação e sentaram a um lado; alguns
anunciaram o seu nome e clã e sentaram a um lado. Alguns permaneceram em
silêncio e sentaram a um lado. Uma vez sentados, o Abençoado disse:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. “Chefes
de família, se os errantes de outras seitas perguntarem a vocês: ‘Chefes de
família, que tipo de contemplativos e brâmanes não devem ser honrados,
respeitados, reverenciados e venerados?’ Vocês deveriam responder assim:
‘Aqueles contemplativos e brâmanes que não estão livres da cobiça, raiva e
delusão em relação a formas percebidas pelo olho, cujas mentes não estão em paz,
e que ora se comportam com<span style="mso-spacerun: yes">  </span>virtude, ora
sem virtude em relação ao corpo, linguagem e mente – esses contemplativos e
brâmanes não devem ser honrados, respeitados, reverenciados e venerados. Por que
isso? Porque nós mesmos não estamos livres da cobiça, raiva e delusão em
relação a formas percebidas pelo olho, nossas mentes não estão em paz, e nós
mesmos ora nos comportamos com<span style="mso-spacerun: yes">  </span>virtude,
ora sem virtude em relação ao corpo, linguagem e mente. Como nós não vemos
nenhum comportamento virtuoso superior por parte desses bons contemplativos e
brâmanes, eles não devem ser honrados, respeitados, reverenciados e venerados. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’Aqueles
contemplativos e brâmanes que não estão livres da cobiça, raiva e delusão em
relação a sons percebidos pelo ouvido … em relação a aromas percebidos pelo
nariz … em relação a sabores percebidos pela língua … em relação a tangíveis
percebidos pelo corpo … em relação a objetos mentais percebidos pela mente,
cujas mentes não estão em paz, e que ora se comportam com<span
style="mso-spacerun: yes">  </span>virtude, ora sem virtude em relação ao
corpo, linguagem e mente ... não devem ser honrados ... Como nós não vemos
nenhum comportamento virtuoso superior por parte desses bons contemplativos e
brâmanes, eles não devem ser honrados, respeitados, reverenciados e venerados.’
Sendo perguntados, chefes de família, assim é como vocês deveriam responder aos
errantes de outras seitas.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. “Chefes
de família, se os errantes de outras seitas perguntarem a vocês: ‘Chefes de
família, que tipo de contemplativos e brâmanes devem ser honrados, respeitados,
reverenciados e venerados?’ Vocês deveriam responder assim: ‘Aqueles
contemplativos e brâmanes que estão livres da cobiça, raiva e delusão em
relação a formas percebidas pelo olho, cujas mentes estão em paz, e que se
comportam com<span style="mso-spacerun: yes">  </span>virtude em relação ao
corpo, linguagem e mente – esses contemplativos e brâmanes devem ser honrados,
respeitados, reverenciados e venerados. Por que isso? Porque nós mesmos não
estamos livres da cobiça, raiva e delusão em relação a formas percebidas pelo
olho, nossas mentes não estão em paz, e nós mesmos ora nos comportamos com<span
style="mso-spacerun: yes">  </span>virtude, ora sem virtude em relação ao
corpo, linguagem e mente. Como nós vemos um comportamento virtuoso superior por
parte desses bons contemplativos e brâmanes, eles devem ser honrados,
respeitados, reverenciados e venerados.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’Aqueles
contemplativos e brâmanes que estão livres da cobiça, raiva e delusão em
relação a sons percebidos pelo ouvido … em relação a aromas percebidos pelo
nariz … em relação a sabores percebidos pela língua … em relação a tangíveis
percebidos pelo corpo … em relação a objetos mentais percebidos pela mente,
cujas mentes estão em paz, e que se comportam com<span style="mso-spacerun:
yes">  </span>virtude em relação ao corpo, linguagem e mente ... devem ser
honrados ... Como nós vemos um comportamento virtuoso superior por parte desses
bons contemplativos e brâmanes, eles devem ser honrados, respeitados,
reverenciados e venerados.’ Sendo perguntados, chefes de família, assim é como
vocês deveriam responder aos errantes de outras seitas<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. “Chefes
de família, se os errantes de outras seitas perguntarem a vocês: ‘Mas quais são
as suas razões, qual é a sua evidência, em relação a esses veneráveis, que faz
com que vocês digam a respeito deles: “Com certeza esses veneráveis ou estão
livres da cobiça ou estão praticando para remover a cobiça; eles estão livres
da raiva ou estão praticando para remover a raiva; estão livres da delusão ou
estão praticando para remover a delusão”?’ – sendo assim perguntados, vocês
deveriam responder a esses errantes de outras seitas desta forma: ‘É porque
esses veneráveis recorrem aos bosques cerrados, afastados. Pois lá não existem
formas percebidas pelo olho do tipo que eles pudessem olhar e se deliciar. Não
existem sons percebidos pelo ouvido do tipo que eles pudessem ouvir e se
deliciar. Não existem aromas percebidos pelo nariz do tipo que eles pudessem
cheirar e se deliciar. Não existem sabores percebidos pela língua do tipo que
eles pudessem saborear e se deliciar. Não existem tangíveis percebidos pelo
corpo do tipo que eles pudessem tocar e se deliciar. Essas são as nossas
razões, amigos, essa é a nossa evidência que faz com que digamos desses
veneráveis: “Com certeza esses veneráveis ou estão livres da cobiça, raiva e
delusão ou estão praticando para removê-las.’” Sendo assim perguntados, vocês
deveriam responder a esses errantes de outras seitas desta forma.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. Quando
isso foi dito, os brâmanes chefes de família de Nagaravinda disseram para o
Abençoado: “Magnífico, Mestre Gotama! Magnífico, Mestre Gotama! Mestre Gotama
esclareceu o Dhamma de várias formas, como se tivesse colocado em pé o que
estava de cabeça para baixo, revelasse o que estava escondido, mostrasse o
caminho para alguém que estivesse perdido ou segurasse uma lâmpada no escuro
para aqueles que possuem visão pudessem ver as formas. Nós buscamos refúgio no
Mestre Gotama, no Dhamma e na Sangha dos bhikkhus. Que o Mestre Gotama nos
aceite como discípulos leigos que nele buscaram refúgio para o resto das nossas
vidas.”<o:p></o:p></span></p>



