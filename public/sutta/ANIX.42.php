<p class=Normal> Assim ouvi. Certa ocasião o venerável Ananda estava em Kosambi no Parque de Ghosita. Então o venerável Udayi foi até o venerável Ananda e eles se cumprimentaram, quando a conversa amigável e cortês havia terminado ele sentou a um lado e disse para o venerável Ananda: </p>

<p class=Normal> "Isto foi dito pelo jovem deva Pancalacanda: [<a href="#N1">1</a>] </p> 

<p class=Normal style='margin-left:1.0in'><a name=R1></a> “'Aquele com ampla sabedoria de fato encontrou <br>
	a passagem no meio do confinamento, <br>
	o Buda que descobriu os jhanas, <br>
	o solitário touro entre os homens, o sábio.'</p>

<p class=Normal> "Amigo, a que se referiu o Abençoado com 'confinamento' e ' a passagem no meio do confinamento'? </p>

<p class=Normal> "Amigo, o Abençoado se referiu aos cinco objetos do prazer sensual como confinamento. Quais cinco? Formas percebidas pelo olho que são desejáveis, agradáveis e fáceis de serem gostadas, conectadas com o desejo sensual e que provocam a cobiça. Sons percebidos pelo ouvido ... Aromas percebidos pelo nariz ... Sabores percebidos pela língua ... Tangíveis percebidos pelo corpo que são desejáveis, agradáveis e fáceis de serem gostados, conectados com o desejo sensual e que provocam a cobiça. O Abençoado se referiu a esses cinco objetos do prazer sensual como confinamento. [<a href="#N2">2</a>] </p>

<p class=Normal> <a name=R2></a> (1) 'Aqui, amigo, afastado dos prazeres sensuais, afastado das qualidades não hábeis, um bhikkhu entra e permanece no primeiro jhana ... foi com referência a isso que o Abençoado se referiu à passagem no meio do confinamento, num sentido provisório. Nisso também há confinamento. Qual é esse confinamento? Qualquer pensamento aplicado e pensamento sustentado que não tenha cessado, esse é o confinamento nesse caso. </p>

<p class=Normal> (2) 'Outra vez, amigo, abandonando o pensamento aplicado e sustentado, um bhikkhu entra e permanece no segundo jhana ... foi com referência a isso que o Abençoado se referiu à passagem no meio do confinamento, num sentido provisório. Nisso também há confinamento. Qual é esse confinamento? Qualquer êxtase que não tenha cessado, esse é o confinamento nesse caso. </p>

<p class=Normal> (3) 'Outra vez, amigo, abandonando o êxtase, um bhikkhu entra e permanece no terceiro jhana ... foi com referência a isso que o Abençoado se referiu à passagem no meio do confinamento, num sentido provisório. Nisso também há confinamento. Qual é esse confinamento? Qualquer prazer conectado com a equanimidade que não tenha cessado, esse é o confinamento nesse caso. </p>

<p class=Normal> (4) 'Outra vez, amigo, com o completo desaparecimento da felicidade, um bhikkhu entra e permanece no quarto jhana ... foi com referência a isso que o Abençoado se referiu à passagem no meio do confinamento, num sentido provisório. Nisso também há confinamento. Qual é esse confinamento? Qualquer percepção da forma que não tenha cessado, esse é o confinamento nesse caso. </p>

<p class=Normal> (5) 'Outra vez, amigo, com a completa superação das percepções da forma, com o desaparecimento das percepções do contato sensorial, sem dar atenção às percepções da diversidade, consciente de que o ‘espaço é infinito,’ ele entra e permanece na base do espaço infinito.  Foi com referência a isso que o Abençoado se referiu à passagem no meio do confinamento, num sentido provisório. Nisso também há confinamento. Qual é esse confinamento? Qualquer percepção da base do espaço infinito que não tenha cessado, esse é o confinamento nesse caso. </p>

<p class=Normal> (6) 'Outra vez, amigo, com a completa superação da base do espaço infinito um bhikkhu entra e permanece na base da consciência infinita.  Foi com referência a isso que o Abençoado se referiu à passagem no meio do confinamento, num sentido provisório. Nisso também há confinamento. Qual é esse confinamento? Qualquer percepção da base da consciência infinita que não tenha cessado, esse é o confinamento nesse caso. </p>

<p class=Normal> (7) 'Outra vez, amigo, com a completa superação da base da consciência infinita, um bhikkhu entra e permanece na base do nada.  Foi com referência a isso que o Abençoado se referiu à passagem no meio do confinamento, num sentido provisório. Nisso também há confinamento. Qual é esse confinamento? Qualquer percepção da base do nada que não tenha cessado, esse é o confinamento nesse caso. </p>

<p class=Normal> (8) 'Outra vez, amigo, com a completa superação da base do nada, um bhikkhu entra e permanece na base da nem percepção, nem não percepção.  Foi com referência a isso que o Abençoado se referiu à passagem no meio do confinamento, num sentido provisório. Nisso também há confinamento. Qual é esse confinamento? Qualquer percepção da base da nem percepção, nem não percepç que não tenha cessado, esse é o confinamento nesse caso. </p>

<p class=Normal> (9) 'Outra vez, amigo, com a completa superação da base da nem percepção, nem não percepção, um bhikkhu entra e permanece na cessação da percepção e sensação, e tendo visto com sabedoria, as suas impurezas são destruídas Foi com referência a isso que o Abençoado se referiu à passagem no meio do confinamento, num sentido não-provisório. </p>

