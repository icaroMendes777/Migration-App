<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
há essas cinco faculdades. Quais cinco? A faculdade da convicção, a faculdade da
energia, a faculdade da atenção plena, a faculdade da concentração, a faculdade
da sabedoria.<o:p></o:p></span></p>


<p class=Normal>“E o que, bhikkhus, é a faculdade da convicção? Neste caso,
bhikkhus, o nobre discípulo é uma pessoa com fé, ele deposita fé na iluminação
do Tathagata assim: ‘<span lang=PT-BR style='mso-ansi-language:PT-BR'>De
fato, o Abençoado é um arahant, perfeitamente iluminado, consumado no
verdadeiro conhecimento e conduta, bem-aventurado, conhecedor dos mundos, um
líder insuperável de pessoas preparadas para serem treinadas, mestre de devas
e humanos, desperto, sublime.’ Essa é chamada a faculdade da convicção.</p>


<p class=Normal>“E o que, bhikkhus, é a faculdade da energia? A energia que
alguém obtém com base nos quatro tipos de esforço correto.[<a href="#N1">1</a>]
<a name=R1></a>Essa é chamada a faculdade da energia.</p>


<p class=Normal>“E o que, bhikkhus, é a faculdade da atenção plena?<span
style='mso-ansi-language:PT-BR'> </span>A atenção plena que alguém obtém com
base nos quatro<span lang=PT-BR style='mso-ansi-language:PT-BR'> fundamentos da
atenção plena. </span>Essa é chamada a faculdade da atenção plena<span
lang=PT-BR style='mso-ansi-language:PT-BR'>.<o:p></o:p></span></p>


<p class=Normal>“E o que, bhikkhus, é a faculdade da concentração?<span
style='mso-ansi-language:PT-BR'> </span>Neste caso, bhikkhus, o nobre discípulo
fazendo da renúncia o seu objeto, alcança a concentração, alcança a unificação
da mente.<span style='mso-ansi-language:PT-BR'> </span>Essa é chamada a
faculdade da concentração<span lang=PT-BR style='mso-ansi-language:PT-BR'>.<o:p></o:p></span></p>


<p class=Normal>“E o que, bhikkhus, é a faculdade da <span lang=PT-BR
style='mso-ansi-language:PT-BR'>sabedoria</span>? Neste caso, bhikkhus, o nobre
discípulo é sábio; ele possui a completa compreensão da origem e da cessação, que é
nobre e penetrante, que conduz ao fim do sofrimento<span lang=PT-BR
style='mso-ansi-language:PT-BR'>. </span>Essa é chamada a faculdade da <span
lang=PT-BR style='mso-ansi-language:PT-BR'>sabedoria.<o:p></o:p></span></p>


<p class=Normal>“Essas, bhikkhus, são as cinco faculdades.” <span
lang=PT-BR style='mso-ansi-language:PT-BR'><o:p></o:p></span></p>


