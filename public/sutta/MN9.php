<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim ouvi.
Em certa ocasião, o Abençoado estava em Savathi, no Bosque de Jeta, no Parque
de Anathapindika. Lá o venerável Sariputta dirigiu-se aos monges desta forma:
&quot;Amigos bhikkhus.&quot; – &quot;Amigo,&quot; eles responderam. O venerável
Sariputta disse o seguinte: <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.
&quot;’Alguém que possui o entendimento correto, alguém que possui o
entendimento correto’, assim é dito, amigos. De que forma um nobre discípulo é
alguém com entendimento correto, cujo entendimento é reto, que possui
perfeita claridade, serenidade e confiança no Dhamma e que penetrou este verdadeiro Dhamma?&quot;[<a
href="#N1">1</a>] <a name=R1></a>&quot;De fato, amigo, nós viríamos de uma
longa distância para aprender do venerável Sariputta qual o significado dessa
frase. Seria bom se o venerável Sariputta pudesse explicar o significado dessa
frase. Tendo ouvido dele, os bhikkhus o recordarão.&quot; &quot;Então, amigos,
ouçam e prestem muita atenção àquilo que eu vou dizer.&quot; &quot;Sim,
amigo&quot os bhikkhus responderam. O venerável Sariputta disse o seguinte: <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><a name="T3"></a><b>(O Benéfico
e o Prejudicial) </b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3.
&quot;Quando, amigos, um nobre discípulo compreende o que é prejudicial e a
raiz do que é prejudicial, compreende o que é benéfico e a raiz do que é benéfico, dessa
forma ele é alguém que possui o entendimento correto, cujo entendimento é reto,
possui perfeita claridade, serenidade e confiança no Dhamma e penetrou este verdadeiro Dhamma.&quot; <o:p></o:p></span></p>



<p class=Normal><a name=T4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>4. &quot;E o que, amigos, é prejudicial, a raiz do que é prejudicial, o
que é benéfico, a raiz do que é benéfico? Matar seres vivos é prejudicial;
tomar o que não seja dado é prejudicial; a conduta imprópria em relação aos
prazeres sensuais é prejudicial; a linguagem mentirosa é prejudicial; a
linguagem maliciosa é prejudicial; a linguagem grosseira é prejudicial; a
linguagem frívola é prejudicial; a cobiça é prejudicial; a má vontade é
prejudicial; o entendimento incorreto é prejudicial. A isto se denomina aquilo
que é prejudicial. [<a href="#N2">2</a>] <a name=R2></a><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. &quot;E
qual é a raiz do que é prejudicial? O desejo é a raiz do que é prejudicial; a
raiva é a raiz do que é prejudicial; a delusão é a raiz do que é prejudicial. A
isto se denomina a raiz do que é prejudicial. [<a href="#N3">3</a>] <a name=R3></a><o:p></o:p></span></p>



<p class=Normal><a name=T6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>6. &quot;E o que é benéfico? A abstenção de matar seres vivos é
benéfica; a abstenção de tomar o que não seja dado é benéfica; a abstenção da
conduta imprópria em relação aos prazeres sensuais é benéfica; a abstenção da
linguagem mentirosa é benéfica; a abstenção da linguagem maliciosa é benéfica;
a abstenção da linguagem grosseira é benéfica; a abstenção da linguagem frívola
é benéfica; não cobiçar é benéfico; não ter má vontade é benéfico; o
entendimento correto é benéfico. A isto se denomina aquilo que é benéfico. [<a href="#N4">4</a>]<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><a
name=R4></a>7. &quot;E
qual é a raiz do que é benéfico? O não desejo é a raiz do que é benéfico; a não
raiva é a raiz do que é benéfico; a não delusão é a raiz do que é benéfico. A
isto se denomina a raiz do que é benéfico.</span><span lang=PT-BR
style='font-size:11.0pt;mso-bidi-font-size:12.0pt;mso-ansi-language:PT-BR'> <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8.
&quot;Quando um nobre discípulo compreendeu dessa forma o que é prejudicial e a
raiz do que é prejudicial, o que é benéfico e a raiz do que é benéfico, [<a
href="#N5">5</a>]
<a name=R5></a>ele abandona completamente a tendência subjacente ao desejo
sensual, ele abole a tendência subjacente à aversão, ele extirpa a tendência
subjacente em relação à idéia e presunção  ‘eu sou’, abandonando a ignorância
e fazendo surgir o verdadeiro conhecimento, ele aqui e agora, dá um fim ao
sofrimento. [<a href="#N6">6</a>]<a name=R6></a> Desta forma, também, um nobre discípulo
possui o entendimento correto, possui o entendimento que é reto, possui
perfeita claridade, serenidade e confiança no Dhamma e penetrou este verdadeiro Dhamma.&quot; &nbsp;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(Alimento)</b><o:p></o:p></span></p>



<p class=Normal><a name=T9></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>9. Dizendo, &quot;Muito bem, amigo&quot; os bhikkhus ficaram contentes
e satisfeitos com as palavras do venerável Sariputta. Então eles lhe fizeram
uma outra pergunta: &quot;Mas, amigo, pode haver alguma outra forma pela qual
um nobre discípulo é aquele que possui entendimento correto … e penetrou este
verdadeiro Dhamma?&quot; - &quot;Sim, pode haver, amigos.&quot; <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10.
&quot;Quando, amigos, um nobre discípulo compreende o alimento, a origem do
alimento, a cessação do alimento e o caminho que conduz à cessação do alimento,
dessa forma ele é alguém que possui entendimento correto … e penetrou este
verdadeiro Dhamma.&quot; <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. &quot;E
o que é o alimento, o que é a origem do alimento, o que é a cessação do
alimento e o que é o caminho que conduz à cessação do alimento? Existem quatro
tipos de alimentos para a manutenção dos seres que já nasceram e para o
sustento daqueles que estão em busca de um nascimento. [<a href="#N7">7</a>] <a
name=R7></a>Quais quatro? O alimento comida, grosseira ou sutil, o contato como
o segundo, a volição mental como o terceiro e a consciência como o quarto. Com
o surgimento do desejo surge o alimento. Com a cessação do desejo cessa o
alimento. O caminho que conduz à cessação do alimento é exatamente este Nobre
Caminho Óctuplo; isto é, entendimento correto, pensamento correto, linguagem correta,
ação correta, modo de vida correto, esforço correto, atenção plena correta,
concentração correta. <o:p></o:p></span></p>



<p class=Normal style='tab-stops:7.25in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>12. &quot;Quando um nobre discípulo compreendeu dessa forma o alimento, a
origem do alimento, a cessação do alimento e o caminho que conduz à cessação do
alimento ele abandona completamente a tendência subjacente ao desejo sensual,
ele abole a tendência subjacente à aversão, ele extirpa a tendência subjacente
em relação à idéia e presunção  ‘eu sou’, abandonando a ignorância e fazendo
surgir o verdadeiro conhecimento ele aqui e agora dá um fim ao sofrimento.
Desta forma, também, um nobre discípulo possui o entendimento correto, possui o
entendimento que é reto, possui perfeita claridade, serenidade e confiança no Dhamma e penetrou este
verdadeiro Dhamma &quot; <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(As Quatro
Nobres Verdades)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13.
Dizendo, &quot;Muito bem, amigo&quot; os bhikkhus ficaram contentes e
satisfeitos com as palavras do venerável Sariputta. Então eles lhe fizeram uma
outra pergunta: &quot;Mas, amigo, pode haver alguma outra forma pela qual um
nobre discípulo é aquele que possui entendimento correto … e penetrou este
verdadeiro Dhamma?&quot; - &quot;Sim, pode haver, amigos.&quot; <o:p></o:p></span></p>



<p class=Normal><a name=T14></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>14. &quot;Quando, amigos, um nobre discípulo compreende o sofrimento, a
origem do sofrimento, a cessação do sofrimento e o caminho que conduz à
cessação do sofrimento, dessa forma ele é alguém que possui entendimento
correto … e penetrou este verdadeiro Dhamma.&quot; <o:p></o:p></span></p>



<p class=Normal><a name=T15></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>15. &quot;E o que é sofrimento, a origem do sofrimento, a cessação do
sofrimento e o caminho que conduz à cessação do sofrimento? Nascimento é
sofrimento; envelhecimento é sofrimento; enfermidade é sofrimento; morte é
sofrimento; tristeza, lamentação, dor, angústia e desespero são sofrimento; não
obter o que se deseja é sofrimento; em resumo, os cinco agregados influenciados
pelo apego são sofrimento. A isto se denomina sofrimento.<span
style="mso-spacerun: yes">   </span><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16. &quot;E
qual é a origem do sofrimento? É este desejo que conduz a uma renovada existência, 
acompanhado pela cobiça e pelo prazer, buscando o prazer aqui e ali; isto é, o desejo 
pelos prazeres sensuais, o desejo por ser/existir, o desejo por não ser/existir. 
A isto se denomina a origem do sofrimento.<span
style="mso-spacerun: yes">   </span><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17. &quot;E
o que é a cessação do sofrimento? É o desaparecimento e cessação sem deixar
nenhum vestígio daquele mesmo desejo, abrir mão, descartar, libertar-se, despegar desse mesmo desejo.
A isto se denomina a cessação do sofrimento. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18. &quot;E
o que é o caminho que conduz à cessação do sofrimento? É exatamente este Nobre
Caminho Óctuplo; isto é, entendimento correto … concentração correta. A isto se
denomina o caminho que conduz à cessação do sofrimento. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>19.
