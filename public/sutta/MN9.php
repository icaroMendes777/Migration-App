nobre discípulo possui o entendimento correto,<span style="mso-spacerun: yes"> 
</span>possui o entendimento que é reto, possui perfeita claridade, serenidade e confiança no Dhamma e
penetrou este verdadeiro Dhamma.&quot; <o:p></o:p></span></p>



<p class=Normal><a name=T20></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'><b>(Envelhecimento e Morte)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>20.
