<p class=Normal><a name=T21></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>21. &quot;Quando, amigos, um nobre discípulo compreende o envelhecimento e
a morte, a origem do envelhecimento e da morte, a cessação do envelhecimento e
da morte e o caminho que conduz à cessação do envelhecimento e da morte, dessa
forma ele é alguém que possui entendimento correto … e penetrou este
verdadeiro Dhamma.&quot;</span> [<a href="#N8">8</a>]</span><span lang=PT-BR
style='mso-ansi-language:PT-BR'> <a name=R8></a><o:p></o:p></span></p>



<p class=Normal><a name=T22></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>22. &quot;E o que é envelhecimento e morte, qual é a origem do
envelhecimento e morte, qual é a cessação do envelhecimento e morte, qual é o
caminho que conduz à cessação do envelhecimento e morte? O envelhecimento dos
seres nas diversas classes de seres, a sua idade avançada, os dentes
quebradiços, os cabelos grisalhos, a pele enrugada, o declínio da vida, o
enfraquecimento das faculdades - a isto se chama envelhecimento. O falecimento
dos seres nas várias classes de seres, a sua morte, a dissolução, o
desaparecimento, o morrer, a finalização do tempo, a dissolução dos agregados [<a
href="#N9">9</a>], <a name=R9></a>o cadáver descartado - a isto se denomina morte.
Portanto, esse envelhecimento e essa morte é que se denomina envelhecimento e
morte. Com o surgimento do nascimento existe o surgimento do envelhecimento e
da morte. Com a cessação do nascimento ocorre a cessação do envelhecimento e da
morte. O caminho que conduz à cessação do envelhecimento e da morte é exatamente
este Nobre Caminho Óctuplo; isto é, entendimento correto … concentração correta. <o:p></o:p></span></p>


