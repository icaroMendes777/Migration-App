correto … concentração correta.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>71.
&quot;Quando um nobre discípulo compreendeu dessa forma as impurezas, a origem das
impurezas, a cessação das impurezas e o caminho que conduz à cessação das impurezas
… ele aqui e agora dá um fim ao sofrimento. Desta forma, também, um nobre
discípulo possui o entendimento correto, possui o entendimento que é reto,
possui perfeita claridade, serenidade e confiança no Dhamma e penetrou este verdadeiro Dhamma.&quot; <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o
que disse o venerável Sariputta. Os bhikkhus ficaram satisfeitos e contentes
com as palavras do venerável Sariputta.<o:p></o:p></span></p>


