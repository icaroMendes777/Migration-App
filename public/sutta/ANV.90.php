<p class=Normal> "Bhikkhus, essas cinco coisas conduzem ao declínio de um bhikkhu que é um treinando. Quais cinco? </p>

<p class=Normal> (1) "Aqui, um bhikkhu que é um treinando tem muitas tarefas e obrigações sendo competente em várias tarefas que devem ser feitas, assim ele negligencia o isolamento e não se dedica à tranquilidade da mente. Essa é a primeira coisa que conduz ao declínio de um bhikkhu que é um treinando. </p>

<p class=Normal> (2) "Novamente, um bhikkhu que é um treinando passa o dia em algum trabalho insignificante, assim ele negligencia o isolamento e não se dedica à tranquilidade da mente. Essa é a segunda coisa que conduz ao declínio de um bhikkhu que é um treinando. </p>

<p class=Normal><a name=R1></a>  (3) "Novamente, um bhikkhu que é um treinando tem laços próximos com chefes de família e monásticos, socializando-se de um modo inadequado, típico de pessoas leigas, [<a href="#N1">1</a>] assim ele negligencia o isolamento e não se dedica à tranquilidade da mente. Essa é a terceira coisa que conduz ao declínio de um bhikkhu que é um treinando. </p>

<p class=Normal> (4) "Novamente, um bhikkhu que é um treinando vai ao vilarejo cedo demais e regressa tarde demais, assim ele negligencia o isolamento e não se dedica à tranquilidade da mente. Essa é a quarta coisa que conduz ao declínio de um bhikkhu que é um treinando. </p>

<p class=Normal> (5) "Novamente, um bhikkhu que é um treinando não ouve de acordo com a sua vontade, sem problemas ou dificuldades, conversa sobre o ascetismo que favorece a abertura do coração, isto é, falar sobre: poucos desejos, contentamento, isolamento, não ficar ligado [com outros], despertar a energia, comportamento virtuoso, concentração, sabedoria, libertação, conhecimento e visão da libertação; assim ele negligencia o isolamento e não se dedica à tranquilidade da mente. Essa é a quinta coisa que conduz ao declínio de um bhikkhu que é um treinando. </p>

<p class=Normal> "Essas cinco coisas conduzem ao declínio de um bhikkhu que é um treinando. </p>

<p class=Normal> "Bhikkhus, essas cinco coisas conduzem ao não-declínio de um bhikkhu que é um treinando. Quais cinco? </p>

<p class=Normal> (1) "Aqui, um bhikkhu que é um treinando não tem muitas tarefas e obrigações; embora ele seja competente em várias tarefas que devem ser feitas, ele não negligencia o isolamento mas se dedica à tranquilidade da mente. Essa é a primeira coisa que conduz ao não-declínio de um bhikkhu que é um treinando. </p>

<p class=Normal> (2) "Novamente, um bhikkhu que é um treinando não passa o dia em algum trabalho insignificante, assim ele não negligencia o isolamento mas se dedica à tranquilidade da mente. Essa é a segunda coisa que conduz ao não-declínio de um bhikkhu que é um treinando. </p>

<p class=Normal> (3) "Novamente, um bhikkhu que é um treinando não tem laços próximos com chefes de família e monásticos, não se socializa de um modo inadequado, típico de pessoas leigas, assim ele não negligencia o isolamento mas se dedica à tranquilidade da mente. Essa é a terceira coisa que conduz ao não-declínio de um bhikkhu que é um treinando. </p>

<p class=Normal> (4) "Novamente, um bhikkhu que é um treinando não vai ao vilarejo cedo demais e não regressa tarde demais, assim ele não negligencia o isolamento mas se dedica à tranquilidade da mente. Essa é a quarta coisa que conduz ao não-declínio de um bhikkhu que é um treinando. </p>

<p class=Normal> (5) "Novamente, um bhikkhu que é um treinando ouve de acordo com a sua vontade, sem problemas ou dificuldades, conversa sobre o ascetismo que favorece a abertura do coração, isto é, falar sobre: poucos desejos, contentamento, isolamento, não ficar ligado [com outros], despertar a energia, comportamento virtuoso, concentração, sabedoria, libertação, conhecimento e visão da libertação; assim ele não negligencia o isolamento mas se dedica à tranquilidade da mente. Essa é a quinta coisa que conduz ao não-declínio de um bhikkhu que é um treinando. </p>

<p class=Normal> "Essas cinco coisas conduzem ao não-declínio de um bhikkhu que é um treinando."  </p>

