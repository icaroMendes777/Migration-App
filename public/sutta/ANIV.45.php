<p class=Normal> Em certa ocasião o Abençoado estava em Savathi no Bosque de Jeta, no Parque de Anathapindika. Então, quando a noite estava bem avançada, o jovem deva Rohitassa, com belíssima aparência, que iluminou todo o Bosque de Jeta, se aproximou do Abençoado. Ao se aproximar ele homenageou o Abençoado e ficando em pé a um lado disse: </p>

<p class=Normal> “É possível, venerável senhor, através de viagens conhecer ou ver, ou chegar ao fim do mundo, onde a pessoa não nasce, não envelhece, não morre, não falece e não renasce?” </p>

<p class=Normal> “Quanto a esse fim do mundo, amigo, onde a pessoa não nasce, não envelhece, não morre, não falece e não renasce – eu digo que não pode ser conhecido ou visto viajando.” </p>

<p class=Normal> “É maravilhoso, venerável senhor! É admirável, venerável senhor! Quão bem isso foi afirmado pelo Abençoado: ‘Quanto a esse fim do mundo, amigo, ... eu digo que não pode ser conhecido ou visto viajando.’ </p>

<p class=Normal> “Uma vez no passado, venerável senhor, eu fui um vidente chamado Rohitassa, filho de Bhoja, possuindo poderes supra-humanos, capaz de viajar através do céu. Minha velocidade era tal, venerável senhor, que eu podia me mover tão rápido quanto uma flecha de luz que atravessasse a sombra de uma palmeira atirada por um arqueiro bem treinado, habilidoso, experimentado. Meu passo era tal, venerável senhor, que parecia alcançar do oceano do leste ao oceano do oeste. Então, venerável senhor, o desejo surgiu em mim: ‘Eu alcançarei o fim do mundo viajando.’ Possuindo tamanha velocidade e tamanho passo, e tendo um tempo de vida de cem anos, vivendo por cem anos, eu viajei por cem anos, sem pausa exceto para comer, beber, defecar, urinar e dissipar a fadiga; no entanto, eu morri no meio do caminho sem ter chegado ao fim do mundo. </p>

<p class=Normal> “É maravilhoso, venerável senhor! É admirável, venerável senhor! Quão bem isso foi afirmado pelo Abençoado: ‘Quanto a esse fim do mundo, amigo, onde a pessoa não nasce, não envelhece, não morre, não falece e não renasce – eu digo que não pode ser conhecido ou visto viajando.’” </p>

<p class=Normal> “No entanto, amigo, eu digo que sem ter alcançado o fim do mundo não há como dar um fim ao sofrimento. Amigo, é exatamente nesta carcaça com uma braça de comprimento, dotada de percepção e mente, que eu revelo o mundo, a origem do mundo, a cessação do mundo e o caminho que conduz à cessação do mundo." </p>

<p class=Normal> O fim do mundo nunca poderá ser alcançado<br>
por meio de viagens [pelo mundo], <br>
no entanto, sem chegar ao fim do mundo <br>
não há libertação do sofrimento. </p>

<p class=Normal> Por conseguinte, o conhecedor do mundo, aquele que é sábio, <br>
que chegou ao fim do mundo, realizou a vida santa, <br>
depois de conhecer o fim do mundo, em paz, <br>
não anseia por este mundo e outro tampouco.</p>

