como vocês deveriam praticar: ‘Nós iremos desenvolver e cultivar a atenção
plena no corpo, fazer dela o nosso veículo, a nossa base, estabilizá-la, nos
exercitarmos nela e aperfeiçoá-la por completo.’ Assim é como vocês deveriam
praticar.”<a name=N6></a><a name=N18></a><o:p></o:p></span></p>

