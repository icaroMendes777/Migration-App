<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não, venerável senhor.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu citei este símile,
