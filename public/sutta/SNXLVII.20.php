bonita deste país!’ uma grande grande multidão de pessoas se aglomerasse.
Agora, aquela<span style='mso-spacerun:yes'>  </span>moça mais bonita do país
dançaria com muita graça e cantaria de forma melodiosa. Ao ouvirem ‘A moça mais
bonita deste país está dançando e cantando! A moça mais bonita deste país está
dançando e cantando!’ uma multidão ainda maior se aglomerasse. Então, surgisse
um homem que valoriza a vida e teme a morte, que deseja o prazer e abomina a
dor. E alguém lhe dissesse, ‘Bom homem, você tem de carregar esta tigela cheia
até a borda com óleo por entre essa grande multidão e a moça mais bonita deste
país. Um homem com uma espada levantada irá segui-lo bem de perto, e se por
acaso você derramar uma gota que seja, nesse mesmo instante, ele cortará a sua
cabeça.’ <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O que
vocês pensam, bhikkhus, aquele homem não irá prestar atenção na tigela com óleo
