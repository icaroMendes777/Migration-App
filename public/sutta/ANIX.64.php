<p class=Normal> “Bhikkhus, há esses cinco obstáculos. Quais cinco? O desejo sensual como um obstáculo, a má vontade como um obstáculo, a preguiça e torpor como um obstáculo, a inquietação e ansiedade como um obstáculo, e a dúvida como um obstáculo. Esses são os cinco obstáculos. </p>

<p class=Normal> “Para abandonar os cinco obstáculos devem ser desenvolvidos os quatro fundamentos da atenção plena. Quais quatro? É o caso em que um bhikkhu permanece contemplando o corpo como um corpo, ardente, plenamente consciente e com atenção plena, tendo colocado de lado a cobiça e o desprazer pelo mundo. Ele permanece contemplando as sensações como sensações, ardente, plenamente consciente e com atenção plena, tendo colocado de lado a cobiça e o desprazer pelo mundo. Ele permanece contemplando a mente como mente, ardente, plenamente consciente e com atenção plena, tendo colocado de lado a cobiça e o desprazer pelo mundo. Ele permanece contemplando os objetos mentais como objetos mentais, ardente, plenamente consciente e com atenção plena, tendo colocado de lado a cobiça e o desprazer pelo mundo. Para abandonar os cinco obstáculos devem ser desenvolvidos os quatro fundamentos da atenção plena.” </p>


