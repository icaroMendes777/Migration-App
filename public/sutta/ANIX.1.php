<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em certa
ocasião o Abençoado estava em <a name=savatthi>Savatthi</a> no Bosque de Jeta,
no parque de Anathapindika. “Se errantes membros de outras
seitas lhe perguntassem, ‘O que, amigo, são as condições para o
desenvolvimento dos requisitos para a perfeita iluminação?’ como vocês responderiam?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável senhor, os nossos ensinamentos têm o Abençoado como origem, como guia e como refúgio. Seria bom
se o Abençoado pudesse explicar o significado dessas palavras. Tendo ouvido do
Abençoado, os bhikkhus o recordarão.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Nesse caso, bhikkhus,
ouçam e prestem muita atenção àquilo que eu vou dizer.” – “Sim, venerável
senhor,” os bhikkhus responderam. O Abençoado disse o seguinte:<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><span style="mso-spacerun:
yes"> </span>“Se errantes membros de outras seitas lhe perguntassem, ‘O que,
amigo, são as condições para o
desenvolvimento dos requisitos para a perfeita iluminação?’ Vocês deveriam responder, ‘É o caso em que um bhikkhu tem pessoas
admiráveis como bons amigos, companheiros e camaradas. Essa é a primeira condição para o
desenvolvimento dos requisitos para a perfeita iluminação. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Além disso, o bhikkhu é
virtuoso. Ele permanece contido de acordo com o Patimokkha,
perfeito na conduta e na sua esfera de atividades. Temendo a menor falha, ele
treina adotando os preceitos de virtude. Essa é a segunda condição para o
desenvolvimento dos requisitos para a perfeita iluminação.<span style="mso-spacerun: yes"> 
</span><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Além disso, ele ouve de
acordo com sua vontade, sem problemas ou dificuldades, conversa sobre o ascetismo que favorece a abertura do coração, isto é, falar sobre: poucos desejos,  contentamento, isolamento, não ficar ligado [com outros], despertar a energia, comportamento virtuoso, concentração, sabedoria, libertação, conhecimento e visão da libertação. Essa é a terceira condição para o
desenvolvimento dos requisitos para a perfeita iluminação. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Além disso, ele permanece
com a sua energia estimulada para o abandono de qualidades mentais prejudiciais
e para a obtenção de qualidades mentais benéficas; ele é decidido, firme no seu
esforço, sem se esquivar das suas responsabilidades com relação ao cultivo de
qualidades mentais benéficas. Essa é a quarta condição para o
desenvolvimento dos requisitos para a perfeita iluminação. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Além disso, ele tem
sabedoria, com a completa compreensão da origem e cessação - nobre, penetrante,
que conduz ao fim do sofrimento. Essa é a quinta condição para o
desenvolvimento dos requisitos para a perfeita iluminação. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Bhikkhus, quando um
bhikkhu tem pessoas admiráveis como bons amigos, companheiros e camaradas,
pode-se esperar que ele será virtuoso, será contido de acordo com o Patimokkha,
perfeito na conduta e na sua esfera de atividades. Temendo a menor falha, ele
treinará adotando os preceitos de virtude. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando um bhikkhu tem
pessoas admiráveis como bons amigos, companheiros e camaradas, pode-se esperar
que ele ouvirá de acordo com sua vontade, sem problemas ou dificuldades, conversa sobre o ascetismo que favorece a abertura do coração, isto é, falar sobre: poucos desejos,  contentamento, isolamento, não ficar ligado [com outros], despertar a energia, comportamento virtuoso, concentração, sabedoria, libertação, conhecimento e visão da libertação. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando um bhikkhu tem
pessoas admiráveis como bons amigos, companheiros, e camaradas, pode-se esperar
que ele permaneça com a sua energia estimulada para o abandono de qualidades
mentais prejudiciais e para a obtenção de qualidades mentais benéficas; ele
será decidido, firme no seu esforço, sem se esquivar das suas responsabilidades
com relação ao cultivo de qualidades mentais benéficas. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><span style="mso-spacerun:
yes"> </span>“Quando um bhikkhu tem pessoas admiráveis como bons amigos,
companheiros, e camaradas, pode-se esperar que ele terá completa compreensão da
origem e cessação - nobre, penetrante, que conduz ao fim do
sofrimento.&quot;<span style="mso-spacerun: yes">  </span><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E além disso, bhikkhus,
quando um bhikkhu está estabelecido com essas cinco qualidades, existem quatro
qualidades adicionais que ele deveria desenvolver: Ele deveria desenvolver a
contemplação daquilo que é repulsivo de forma a abandonar a cobiça. Ele deveria
desenvolver o amor bondade de forma a abandonar a má vontade. Ele deveria
desenvolver a atenção plena na respiração de forma a eliminar os pensamentos
que distraem. Ele deveria desenvolver a percepção da impermanência de forma a
eliminar a presunção ‘eu sou.’ Em um bhikkhu que perceba a impermanência, a
percepção do não-eu se firma. Aquele que percebe o não-eu realiza o
desenraizamento da presunção ‘eu sou’ – a Libertação no aqui e agora.” <o:p></o:p></span></p>


