<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim ouvi.
Em certa ocasião, o Abençoado estava entre os Sakyas numa cidade denominada
Nagaraka. Então, o venerável Ananda foi até o Abençoado e depois de
cumprimentá-lo sentou a um lado e disse: <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, isto é metade da vida santa, ter pessoas admiráveis como bons amigos,
companheiros e camaradas.” [<a href="#N1">1</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não diga
isso, Ananda. Não diga isso, Ananda. Isso é toda a vida santa, Ananda, isto é,
ter pessoas admiráveis como bons amigos, companheiros e camaradas. Quando um
bhikkhu tem pessoas admiráveis como bons amigos, companheiros e camaradas, é de
se esperar que ele desenvolva e se dedique ao nobre caminho óctuplo.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como, Ananda, um
bhikkhu, que tem pessoas admiráveis como bons amigos, companheiros e camaradas,
desenvolve e se dedica ao nobre caminho óctuplo? Aqui, Ananda, um bhikkhu
desenvolve o entendimento correto, que tem por base o afastamento, o desapego e
a cessação, que amadurece no abandono.[<a href="#N2">2</a>] <a name=R2></a>Ele
desenvolve o pensamento correto ... linguagem correta ... ação correta ... modo
de vida correto ... esforço correto ... atenção plena correta ... concentração
correta, que tem por base o afastamento, o desapego, a cessação, que amadurece
no abandono. Assim é como um bhikkhu, que tem pessoas admiráveis como bons
amigos, companheiros e camaradas, desenvolve e se dedica ao nobre caminho
óctuplo.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E seguindo esse método,
Ananda, também é possível compreender como toda a vida santa é ter pessoas
admiráveis como bons amigos, companheiros e camaradas: é contando comigo como
um bom amigo que os seres sujeitos ao nascimento se libertam do nascimento, que
os seres sujeitos ao envelhecimento se libertam do envelhecimento, que os seres
sujeitos à morte se libertam da morte, que os seres sujeitos à tristeza,
lamentação, dor, angústia e desespero se libertam da tristeza, lamentação, dor,
angústia e desespero. Seguindo esse método, Ananda, é possível compreender como
toda a vida santa é ter pessoas admiráveis como bons amigos, companheiros e
camaradas.”<o:p></o:p></span></p>


