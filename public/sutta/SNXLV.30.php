<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. Então, o Venerável Uttiya foi até o Abençoado e depois de cumprimentá-lo
sentou a um lado e disse:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
venerável senhor, enquanto eu estava só, em isolamento, este pensamento surgiu
na minha mente: ‘Os cinco elementos do prazer sensual foram descritos pelo
Abençoado. Mas o que então são esses cinco elementos do prazer sensual?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Muito bem,
Uttiya! Esses cinco elementos do prazer sensual foram descritos por mim. Quais
cinco? Formas conscientizadas através do olho, que são desejáveis, agradáveis e
fáceis de serem gostadas, conectadas com o desejo sensual e que provocam a
cobiça. Sons conscientizados através do ouvido … Aromas conscientizados através do nariz
… Sabores conscientizados através da língua …Tangíveis conscientizados através do corpo,
que são desejáveis, agradáveis e fáceis de serem gostados, conectados com o
desejo sensual e que provocam a cobiça. Esses são os cinco elementos do prazer
sensual descritos por mim.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O Nobre
Caminho Óctuplo, Uttiya, deve ser desenvolvido para o abandono desses cinco
elementos do prazer sensual. E qual é o Nobre Caminho Óctuplo? É o entendimento
correto ... concentração correta. O Nobre Caminho Óctuplo deve ser desenvolvido
para o abandono desses cinco elementos do prazer sensual.”<o:p></o:p></span></p>


