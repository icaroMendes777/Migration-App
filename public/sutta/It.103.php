<p class=Normal> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: "Bhikkhus todos contemplativos e brâmanes que não compreendem como na verdade é: 'Isto é sofrimento'; 'Esta é a origem do sofrimento'; 'Esta é a cessação do sofrimento'; 'Este é o caminho que conduz à cessação do sofrimento' - esses contemplativos e brâmanes não são considerados por mim serem verdadeiros contemplativos entre contemplativos, serem verdadeiros brâmanes entre brâmanes. Esses veneráveis permanecem sem terem realizado por si mesmos, aqui e agora, através do conhecimento direto, o objetivo  de ser um contemplativo, o objetivo de ser um brâmane.</p>

<p class=Normal> "Mas, bhikkhus todos contemplativos e brâmanes que compreendem como na verdade é: 'Isto é sofrimento'; 'Esta é a origem do sofrimento'; 'Esta é a cessação do sofrimento'; 'Este é o caminho que conduz à cessação do sofrimento' - esses contemplativos e brâmanes são considerados por mim serem verdadeiros contemplativos entre contemplativos, serem verdadeiros brâmanes entre brâmanes. Esses veneráveis permanecem tendo realizado por si mesmos, aqui e agora, através do conhecimento direto, o objetivo  de ser um contemplativo, o objetivo de ser um brâmane."</p>

<p class=Normal style='margin-left:.75in'> Aqueles que não compreendem o sofrimento, <br>
ou como o sofrimento é produzido, <br>
ou onde o sofrimento finalmente cessa<br>
sem deixar vestígios, <br>
e que não conhecem o caminho, <br>
que conduz ao fim do sofrimento -<br>
eles estão desprovidos da libertação da mente<br>
e também carecem da libertação através da sabedoria; <br>
incapazes de dar um fim, <br>
eles seguem para o nascimento e morte.</p>

<p class=Normal style='margin-left:.75in'> Mas aqueles que compreendem o sofrimento, <br>
e como o sofrimento é produzido, <br>
e onde o sofrimento finalmente cessa<br>
sem deixar vestígios, <br>
e que conhecem o caminho, <br>
que conduz ao fim do sofrimento -<br>
eles possuem a libertação da mente<br>
e também a libertação através da sabedoria; <br>
capazes de dar um fim, <br>
eles nunca mais regressam para o nascimento e morte.</p>



