<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. [</span><a href="#N1"><span lang=PT-BR style='mso-ansi-language:PT-BR'>1</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R1></a>Em certa ocasião o
Abençoado estava em Savatthi no Bosque de Jeta, no Parque de Anathapindika. Lá
ele se dirigiu aos monges desta forma: “Bhikkhus” – “Venerável Senhor,” eles
responderam. O Abençoado disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(ESPECULAÇÕES
ACERCA DO FUTURO)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.
“Bhikkhus, há alguns contemplativos e brâmanes que especulam acerca do futuro,
que possuem idéias acerca do futuro, que fazem várias alegações doutrinárias em
relação ao futuro.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(I) Alguns
afirmam o seguinte: ‘O eu é perceptivo e intacto [<a href="#N2">2</a>] <a
name=R2></a>após a morte.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(II) Alguns
afirmam o seguinte: ‘O eu é não-perceptivo e intacto após a morte.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(III)
Alguns afirmam o seguinte: ‘O eu é nem perceptivo, nem não-perceptivo e
intacto após a morte.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(IV) Ou
eles descrevem a aniquilação, destruição e exterminação de um ser existente [na
morte].<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(V) Ou
alguns afirmam Nibbana aqui e agora.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
(a) ou eles descrevem um eu existente que permanece intacto após a morte; (b)
ou eles descrevem a aniquilação, destruição e exterminação de um ser existente
[na morte]; (c) ou eles afirmam Nibbana aqui e agora. Assim essas [idéias],
sendo cinco se tornam três e sendo três se tornam cinco. Esse é o sumário dos
‘cinco e três.’<o:p></o:p></span></p>



<p class=Normal><a name=T3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>3. (I) “Nesse sentido, bhikkhus, aqueles contemplativos e brâmanes que
descrevem o eu como perceptivo e intacto após a morte, descrevem esse eu,
perceptivo e intacto após a morte, como um dos seguintes:<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>material;<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>ou
imaterial;<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>ou ambos,
material e imaterial;<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>ou nem
material e tampouco imaterial; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>ou
perceptivo da unidade; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>ou
perceptivo da diversidade;<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>ou
perceptivo do limitado;<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>ou
perceptivo do imensurável. [<a href="#N3">3</a>] <o:p></o:p></span></p>

<p class=Normal><a name=R3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Ou ainda, dentre aqueles poucos que vão além disso, alguns afirmam ser a
kasina da consciência, imensurável e imperturbável, [o eu]. [<a href="#N4">4</a>]
<o:p></o:p></span></p>



<p class=Normal><a name=R4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>4. “O Tathagata, bhikkhus, compreende isso da seguinte forma: ‘Esses
bons contemplativos e brâmanes que descrevem o eu como perceptivo e intacto
após a morte, descrevem esse eu,<span style='mso-spacerun:yes'> 
</span>perceptivo e intacto após a morte, como, ou material ... ou eles o
descrevem como perceptivo do imensurável. Por outro lado, visto que [a
percepção] “não há nada” é declarada como a mais pura, suprema, a melhor e
insuperável dessas percepções – sejam elas, percepção da forma ou sem forma, da
unidade ou diversidade [<a href="#N5">5</a>] – <a name=R5></a>alguns afirmam
ser a base do nada, imensurável e imperturbável [o eu]. Isso é condicionado e
grosseiro, mas há a cessação das formações.’ Tendo compreendido ‘Existe isso,’
vendo a escapatória disso, o Tathagata foi além disso. [<a href="#N6">6</a>] <o:p></o:p></span></p>



<p class=Normal><a name=R6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>5. (II) “Nesse sentido, bhikkhus, aqueles contemplativos e brâmanes que
descrevem o eu como não-perceptivo e intacto após a morte, descrevem esse eu
não-perceptivo e intacto após a morte como um dos seguintes:<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>material;<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>ou
imaterial;<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>ou ambos, material
e imaterial;<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>ou nem
material e tampouco imaterial. [<a href="#N7">7</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R7></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>6. “Nesse sentido, bhikkhus, esses criticam aqueles contemplativos e
brâmanes que descrevem o eu como perceptivo e intacto após a morte. Por que isso?
Porque eles dizem: ‘A percepção é uma enfermidade, a percepção é um tumor, a
percepção é uma flecha; isto é pacífico, isto é sublime, isto é, a
não-percepção.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. “O
Tathagata, bhikkhus, compreende isso da seguinte forma: ‘Esses bons
contemplativos e brâmanes que descrevem o eu como não-perceptivo e intacto após
a morte, descrevem esse eu não-perceptivo e intacto após a morte como, ou
material ... ou nem material e tampouco imaterial. Que algum contemplativo ou
brâmane possa dizer: “Separado da forma material, separado da sensação,
separado da percepção, separado das formações, eu irei descrever as vindas e
idas da consciência, o seu falecimento e renascimento, o seu desenvolvimento,
crescimento e maturação” – isso é impossível. [<a href="#N8">8</a>] <a name=R8></a>Isso
é condicionado e grosseiro, mas há a cessação das formações.’ Tendo
compreendido ‘Existe isso,’ vendo a escapatória disso, o Tathagata foi além
disso. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. (III)
“Nesse sentido, bhikkhus, aqueles contemplativos e brâmanes que descrevem o eu
como nem perceptivo, nem não-perceptivo e intacto após a morte, descrevem esse
eu nem perceptivo, nem não-perceptivo e intacto após a morte como um dos
seguintes:<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>material;<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>ou
imaterial;<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>ou ambos
material e imaterial;<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>ou nem
material e tampouco imaterial. [<a href="#N9">9</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R9></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>9. “Nesse sentido, bhikkhus, esses criticam aqueles contemplativos e
brâmanes que descrevem o eu como perceptivo e intacto após a morte, e eles
criticam aqueles contemplativos e brâmanes que descrevem o eu como
não-perceptivo e intacto após a morte. Por que isso? Porque eles dizem: ‘A
percepção é uma enfermidade, a percepção é um tumor, a percepção é uma flecha,
e a não-percepção é a estupefação; [<a href="#N10">10</a>] <a name=R10></a>isto
é pacífico, isto é sublime, isto é, nem percepção, nem não-percepção.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. “O
Tathagata, bhikkhus, compreende isso da seguinte forma: ‘Esses bons
contemplativos e brâmanes que descrevem o eu como nem perceptivo, nem
não-perceptivo e intacto após a morte, descrevem esse eu, nem perceptivo, nem
não-perceptivo e intacto após a morte, ou como material ... ou nem material e
tampouco imaterial. Se algum contemplativo ou brâmane descrever que a entrada
nessa base ocorre através de uma medida de formações relacionada ao que é
visto, ouvido, sentido e conscientizado, isto é declarado ser um desastre para
a entrada nessa base. [<a href="#N11">11</a>] <a name=R11></a>Pois esta base, é
assim declarado, não é para ser alcançada como uma realização com formações;
esta base, é assim declarado, é para ser alcançada como uma realização com um
resíduo de formações. [<a href="#N12">12</a>] <a name=R12></a>Isso é
condicionado e grosseiro, mas há a cessação das formações.’ Tendo compreendido
‘Existe isso,’ vendo a escapatória disso, o Tathagata foi além disso.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. (IV)
“Nesse sentido, bhikkhus, aqueles contemplativos e brâmanes que descrevem a
aniquilação, destruição e exterminação de um ser existente [na morte] [<a
href="#N13">13</a>] <a name=R13></a>criticam aqueles contemplativos e brâmanes
que descrevem o eu como perceptivo e intacto após a morte, e eles criticam
aqueles contemplativos e brâmanes que descrevem o eu como não-perceptivo e
intacto após a morte, e eles criticam aqueles contemplativos e brâmanes que
descrevem o eu como nem perceptivo, nem não-perceptivo e intacto após a morte.
Por que isso? Todos esses contemplativos e brâmanes, apressando-se adiante,
afirmam o seu apego assim: ‘Nós seremos assim após a morte, nós seremos assim
após a morte.’ Como um comerciante, que vai para o mercado, pensa: ‘Através
disto, aquilo será meu; com isto, eu conseguirei aquilo’; assim também, aqueles
contemplativos e brâmanes parecem comerciantes quando eles declaram: ‘Nós
seremos assim após a morte, nós seremos assim após a morte.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. “O
Tathagata, bhikkhus, compreende isso da seguinte forma: ‘Esses bons
contemplativos e brâmanes que descrevem a aniquilação, destruição e
exterminação de um ser existente [na morte], devido ao medo da identidade e
repulsa pela identidade, ficam correndo e circundando ao redor dessa mesma
identidade. [<a href="#N14">14</a>] <a name=R14></a>Como um cão, atado por uma
correia a um poste firme ou coluna, fica correndo e circundando ao redor desse
mesmo poste ou coluna; assim também, esses contemplativos e brâmanes, devido ao
medo da identidade e repulsa pela identidade, ficam correndo e circundando ao
redor dessa mesma identidade. Isso é condicionado e grosseiro, mas há a
cessação das formações.’ Tendo compreendido ‘Existe isso,’ vendo a escapatória
disso, o Tathagata foi além disso.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13.
“Bhikkhus, quaisquer contemplativos e brâmanes que especulem acerca do futuro,
que possuam idéias acerca do futuro, e que façam várias alegações doutrinárias
com relação ao futuro, todos afirmam essas cinco bases ou alguma dentre elas. [<a
href="#N15">15</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R15></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'><b>(ESPECULAÇÕES ACERCA DO PASSADO)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14.
“Bhikkhus, há alguns contemplativos e brâmanes que especulam acerca do passado,
que possuem idéias acerca do passado, que fazem várias alegações doutrinárias
com relação ao passado.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(1) Alguns
afirmam o seguinte: ‘O eu e o mundo são eternos: somente isto é verdadeiro,
todo o restante é falso.’ [<a href="#N16">16</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R16></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>(2) Alguns afirmam o seguinte: ‘O eu e o mundo não são eternos: somente
isto é verdadeiro, todo o restante é falso.’ [<a href="#N17">17</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R17></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>(3) Alguns afirmam o seguinte: ‘O eu e o mundo são ambos, eterno e não
eterno: somente isto é verdadeiro, todo o restante é falso.’ [<a href="#N18">18</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R18></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>(4) Alguns afirmam o seguinte: ‘O eu e o mundo são nem eternos, nem não
eternos: somente isto é verdadeiro, todo o restante é falso.’ [<a href="#N19">19</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R19></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>(5) Alguns afirmam o seguinte: ‘O eu e o mundo são finitos: somente isto
é verdadeiro, todo o restante é falso.’ [<a href="#N20">20</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R20></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>(6) Alguns afirmam o seguinte: ‘O eu e o mundo são infinitos: somente
isto é verdadeiro, todo o restante é falso.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(7) Alguns
afirmam o seguinte: ‘O eu e o mundo são ambos, finito e infinito: somente isto
é verdadeiro, todo o restante é falso.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(8) Alguns
afirmam o seguinte: ‘O eu e o mundo são nem finitos, nem infinitos: somente
isto é verdadeiro, todo o restante é falso.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(9) Alguns
afirmam o seguinte: ‘O eu e o mundo são perceptivos da unidade: somente isto é
verdadeiro, todo o restante é falso.’ [<a href="#N21">21</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R21></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>(10) Alguns afirmam o seguinte: ‘O eu e o mundo são perceptivos da
diversidade: somente isto é verdadeiro, todo o restante é falso.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(11) Alguns
afirmam o seguinte: ‘O eu e o mundo são perceptivos do limitado: somente isto é
verdadeiro, todo o restante é falso.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(12) Alguns
afirmam o seguinte: ‘O eu e o mundo são perceptivos do imensurável: somente
isto é verdadeiro, todo o restante é falso.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(13) Alguns
afirmam o seguinte: ‘O eu e o mundo [experimentam] exclusivamente o prazer:
somente isto é verdadeiro, todo o restante é falso.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(14) Alguns
afirmam o seguinte: ‘O eu e o mundo [experimentam] exclusivamente a dor:
somente isto é verdadeiro, todo o restante é falso.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(15) Alguns
afirmam o seguinte: ‘O eu e o mundo [experimentam] ambos, prazer e dor: somente
isto é verdadeiro, todo o restante é falso.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(16) Alguns
afirmam o seguinte: ‘O eu e o mundo [experimentam] nem prazer, nem dor: somente
isto é verdadeiro, todo o restante é falso.' <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. (1)
“Nesse sentido, bhikkhus, com relação àqueles contemplativos e brâmanes que
possuem a seguinte doutrina e entendimento: ‘O eu e o mundo são eternos:
somente isto é verdadeiro, todo o restante é falso,’ que exceto pela fé, exceto
pela preferência, exceto pela tradição, exceto pela cogitação com base na razão, exceto pelas
idéias, eles terão um tipo de conhecimento pessoal claro e puro disso – isso é
impossível. [<a href="#N22">22</a>] <a name=R22></a>Visto que eles não possuem
conhecimento pessoal claro e puro, mesmo o mero conhecimento incompleto que
esses contemplativos e brâmanes elucidam [acerca do entendimento deles] é
declarado como sendo apego da parte deles. [<a href="#N23">23</a>] <a name=R23></a>Isso
é condicionado e grosseiro, mas há a cessação das formações.’ Tendo
compreendido ‘Existe isso,’ vendo a escapatória disso, o Tathagata foi além disso.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16. (2-16)
“Nesse sentido, bhikkhus, aqueles contemplativos e brâmanes que possuem a
seguinte doutrina e entendimento: ‘O eu e o mundo não são eternos ... ambos,
eterno e não eterno ... nem eternos, nem não eternos ... finitos ... infinitos
... ambos, finito e infinito ... nem finitos, nem infinitos ... perceptivos da
unidade ... perceptivos da diversidade ... perceptivos do limitado ...
perceptivos do imensurável ... [experimentam] exclusivamente o prazer ...
[experimentam] exclusivamente a dor ... [experimentam] ambos, prazer e dor ...
[experimentam] nem prazer, nem dor: que exceto pela fé, exceto pela
preferência, exceto pela tradição,<span style='mso-spacerun:yes'> 
</span>exceto pela cogitação com base na razão, exceto pelas idéias, eles terão algum tipo de
conhecimento pessoal claro e puro disso – isso é impossível. Visto que eles não
possuem conhecimento pessoal claro e puro, mesmo o mero conhecimento incompleto
que esses contemplativos e brâmanes elucidam [acerca do entendimento deles] é
declarado como sendo apego da parte deles. Isso é condicionado e grosseiro, mas
há a cessação das formações.’ Tendo compreendido ‘Existe isso,’ vendo a
escapatória disso, o Tathagata foi além disso. [<a href="#N24">24</a>]<o:p></o:p></span></p>



