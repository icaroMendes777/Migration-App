treinar, bhikkhus.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12.
“Bhikkhus, suponham que um homem viesse com uma enxada e um cesto e dissesse:
‘Eu farei com que este grande planeta Terra fique sem <span class=GramE>terra.</span>’
