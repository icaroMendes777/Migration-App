Parque de Anathapindika.<o:p></o:p></span></p>



<p class=Normal><a name=T2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2. Agora, naquela ocasião o venerável Moliya Phagguna estava se
associando em demasia com as <span class=GramE>bhikkhunis.</span>[</span><a
href="#N1"><span lang=PT-BR style='mso-ansi-language:PT-BR'>1</span></a><span
