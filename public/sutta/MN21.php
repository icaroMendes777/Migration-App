Terra ficasse sem terra?” – “Não, venerável senhor.” “Por que? Porque este
planeta Terra é demasiado profundo e imenso, não é possível fazer com que ele
fique sem terra. No final o homem só iria colher cansaço e desapontamento.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. “Da
