fala dele poderá ser verdadeira ou falsa; quando alguém se dirigir a
vocês a fala dele poderá ser gentil ou grosseira; quando alguém se dirigir a vocês a fala dele poderá ser conectada com o benéfico ou com o
prejudicial; quando alguém se dirigir a vocês a fala dele poderá ser dita
com a mente cheia de amor bondade ou com raiva. Nesses casos, bhikkhus, assim é
como vocês deveriam treinar: ‘Nossas mentes não serão afetadas e nós não
diremos palavras ruins; nós permaneceremos compassivos pelo bem-estar dele, com
