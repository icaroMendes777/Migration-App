<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. Então o
Abençoado se dirigiu a <span class=GramE>um certo</span> bhikkhu desta forma:
“Venha, bhikkhu, diga em meu nome, ao bhikkhu Moliya Phagguna que o Mestre o
chama.” “Sim, venerável senhor”, ele respondeu e foi até o venerável Moliya
Phagguna e lhe disse: “O Mestre o chama, amigo Phagguna.” - “Sim, Amigo”, ele
respondeu, indo até o Abençoado e após cumprimentá-lo, sentou a um lado. O
Abençoado então perguntou<span class=GramE>:<span style='mso-spacerun:yes'> 
</span></span><o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5.
“Phagguna, é verdade que você está se associando em demasia com as bhikkhunis,
que você está se associando tanto com as bhikkhunis que se algum bhikkhu
critica aquelas bhikkhunis na sua presença, você fica furioso e desgostoso e o
censura; e se algum bhikkhu critica o venerável Phagguna na presença daquelas
bikkhunis, elas ficam furiosas e desgostosas e o censuram. Você está se
associando tanto com as bikkhunis quanto parece?” – “Sim venerável senhor.” –
“Phagguna, você não é um membro de um clã que pela fé deixou a vida em família
pela vida santa?” – “Sim, venerável senhor.<span class=GramE>”</span><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. “Phagguna,
não é adequado que você, um membro de um clã que pela fé deixou a vida em
família pela vida santa, se associe em demasia com as bhikkhunis. Então, se
alguém criticar aquelas bhikkhunis na sua presença, você deve abandonar todos
os desejos e pensamentos que tomem por base a vida em família. E assim é como
você deveria treinar: ‘Minha mente não será afetada e eu não direi palavras
ruins; eu permanecerei compassivo pelo bem-estar dele, com a mente plena de
amor bondade, sem <span class=GramE>raiva.</span>’ Assim é como você deve
treinar, Phagguna.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se alguém
golpear as bhikkhunis com a mão, com pedras, com um pau ou com uma faca na sua
presença, você deve abandonar todos os desejos e pensamentos que tomem por base
a vida em família. E assim é como você deveria treinar: ‘Minha mente não será
afetada<span class=GramE><span style='mso-spacerun:yes'>  </span>...</span>’ Se
alguém fizer alguma crítica na sua presença, você deve abandonar todos os
desejos e pensamentos que tomem por base a vida em família. E assim é como você
deveria treinar: ‘Minha mente não será afetada ...’ Se alguém golpeá-lo com a
mão, com pedras, com um pau ou com uma faca, você deve abandonar todos os
desejos e pensamentos que tomem por base a vida em família. E assim é como você
deveria treinar: ‘Minha mente não será afetada e eu não direi palavras ruins;
eu permanecerei compassivo pelo bem-estar dele, com a mente plena de amor
bondade, sem raiva.’ Assim é como você deve se treinar, Phagguna. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. Então o
Abençoado se dirigiu aos bhikkhus da seguinte forma: “Bhikkhus, houve uma
ocasião em que os bhikkhus contentavam a minha mente. Eu me dirigia aos
bhikkhus da seguinte forma: ‘Bhikkhus, eu como uma vez por dia. Fazendo isso,
eu fico livre de enfermidades e aflições, desfruto de boa saúde, energia e
permaneço com conforto. Venham, bhikkhus, comam apenas uma vez por dia. Fazendo
isso, vocês ficarão livres de enfermidades e aflições, <span class=GramE>desfrutarão</span>
de boa saúde, energia e permanecerão com conforto.’ E eu não precisava ficar
instruindo aqueles bhikkhus; eu tinha apenas que despertar a atenção plena
neles.[</span><a href="#N2"><span lang=PT-BR style='mso-ansi-language:PT-BR'>2</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R2></a>Suponham que
houvesse uma carruagem num terreno plano numa encruzilhada, com puros-sangues
arreados e a aguilhada preparada, de forma que um adestrador habilidoso, um
cocheiro domador de cavalos pudesse montar nela e, tomando as rédeas na mão
esquerda e a aguilhada na mão direita, pudesse ir e regressar por qualquer
caminho quando ele quisesse. Da mesma forma, eu não precisava ficar instruindo
aqueles bhikkhus; eu tinha apenas que despertar a atenção plena neles.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8, “Então,
bhikkhus, abandonem o que é prejudicial e se dediquem aos estados benéficos,
pois é assim que vocês <span class=GramE>crescerão,</span> se desenvolverão e
se realizarão neste Dhamma e Disciplina. Suponham que houvesse um grande bosque
de árvores Sal próximo a um vilarejo ou cidade e elas estivessem sufocadas com
ervas daninhas e um homem surgisse desejando o bem, bem-estar e proteção das
árvores. Ele cortaria e jogaria fora as mudas defeituosas que roubam a
seiva,<span style='mso-spacerun:yes'>  </span>ele limparia o interior do bosque
e cuidaria das mudas bem formadas, de forma que mais tarde o bosque de árvores
Sal pudesse crescer, se desenvolver e se realizar. Da mesma forma, bhikkhus,
abandonem o que é prejudicial e se dediquem aos estados benéficos, pois é assim
como vocês crescerão, se desenvolverão e se realizarão neste Dhamma e Disciplina.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9.
“Antigamente, bhikkhus, aqui mesmo em Savatthi havia uma dona de casa chamada
Vedehika. E um bom relato sobre a Senhora Vedehika havia se espalhado: ‘A
Senhora Vedehika é boa, a Senhora Vedehika é gentil, a Senhora Vedehika é <span
class=GramE>pacífica.</span>’ Agora a Senhora Vedehika tinha uma empregada
chamada Kali, que era destra, ágil e perfeita no seu trabalho. A empregada Kali
pensou: ‘Um bom relato sobre a minha senhora tem se espalhado:<span
style='mso-spacerun:yes'>  </span>“A Senhora Vedehika é boa, a Senhora Vedehika
é gentil, a Senhora Vedehika é pacífica,”<span style='mso-spacerun:yes'> 
</span>Como será isso, embora ela não demonstre raiva, a raiva está na verdade
presente nela ou está ausente? Ou será que é apenas devido ao meu trabalho
perfeito que a minha senhora não demonstra a raiva, mas a raiva, no entanto,
está na verdade presente nela? E se eu testasse a minha senhora.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Assim a
empregada Kali se levantou mais tarde. Então a Senhora Vedehika disse: ‘Ei, <span
class=GramE>Kali!</span>’ – ‘O que é, senhora?’ – ‘Qual é o problema, por que
você se levantou tão tarde?’ – ‘Não há nenhum problema, senhora.’ – ‘Não há
nenhum problema, sua garota má, no entanto você levanta tão tarde!’ e ela ficou
furiosa e irritada e olhou com cara feia. Então a empregada Kali pensou: ‘O
fato é que, apesar da minha senhora não demonstrar raiva, a raiva ainda está na
verdade presente nela, não ausente; e é apenas devido ao meu trabalho perfeito
que a minha senhora não demonstra raiva, que na verdade está presente nela, não
ausente. E se eu testasse a minha senhora um pouco mais.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Assim a
empregada Kali se levantou ainda mais tarde. Então a Senhora Vedehika disse:
‘Ei, <span class=GramE>Kali!</span>’ – ‘O que é, senhora?’ – ‘Qual é o
problema, por que você se levantou ainda mais tarde?’ – ‘Não há nenhum
problema, senhora.’ – ‘Não há nenhum problema, sua garota má, no entanto você
levanta ainda mais tarde!’ e ela ficou furiosa e irritada e disse palavras de
desaprovação. Então a empregada Kali pensou: ‘O fato é que, apesar da minha
senhora não demonstrar raiva, a raiva ainda está na verdade presente nela, não
ausente. E se eu testasse a minha senhora um pouco mais.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Assim a
empregada Kali se levantou ainda mais tarde. Então a Senhora Vedehika disse:
‘Ei, <span class=GramE>Kali!</span>’ – ‘O que é, senhora?’ – ‘Qual é o
problema, por que você se levantou ainda mais tarde?’ – ‘Não há nenhum
problema, senhora.’ – ‘Não há nenhum problema, sua garota má, no entanto você
levanta ainda mais tarde!’ e ela ficou furiosa e irritada e tomou um rolo para
massa e golpeou Kali na cabeça, cortando-a.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então a
empregada Kali, com o sangue jorrando da cabeça cortada, denunciou a sua
senhora para os vizinhos: ‘Vejam, senhoras, a obra da bondosa senhora! <span
class=GramE>Vejam,</span> senhoras, a obra da gentil senhora! Vejam, senhoras,
a obra da pacífica senhora! Como ela pode ficar furiosa e irritada com a sua
única empregada por ela se levantar mais tarde? Como ela pode agarrar um rolo
de massa, golpeá-la na cabeça e cortá-la?’ Então mais tarde um relato ruim
sobre a Senhora Vedehika havia se espalhado: ‘A Senhora Vedehika é grosseira, a
Senhora Vedehika é violenta, a Senhora Vedehika é cruel.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. “Da
mesma forma, bhikkhus, um bhikkhu é extremamente bom, extremamente gentil,
extremamente pacífico, contanto que a linguagem desagradável não o toque. Mas é
quando a linguagem desagradável o toca que se pode reconhecer se aquele bhikkhu
é realmente bom, gentil e pacífico. Eu não digo que um bhikkhu<span
class=GramE><span style='mso-spacerun:yes'>  </span></span>seja fácil de ser
censurado se ele for fácil de ser censurado e aceitar a censura apenas com o
propósito de obter mantos, comida esmolada, moradia e medicamentos. Por que
isso?<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Porque esse
bhikkhu não é fácil de ser censurado e nem aceita a censura quando ele não
obtém<span class=GramE><span style='mso-spacerun:yes'>  </span></span>mantos,
comida esmolada, moradia e medicamentos. Mas quando um bhikkhu é fácil de ser
censurado e aceita a censura porque ele honra, respeita e reverencia o Dhamma,
eu digo que ele é fácil de ser censurado. Portanto, bhikkhus, vocês devem
treinar dessa forma: ‘Nós seremos fáceis de ser censurados e aceitaremos a
censura porque nós honramos, respeitamos e reverenciamos o <span class=GramE>Dhamma.</span>’
Assim é como vocês deveriam treinar, bhikkhus.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11.
“Bhikkhus, existem esses cinco tipos de linguagem que alguém pode usar ao
