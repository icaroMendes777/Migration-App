<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, eu ensinarei para vocês o sofrimento, a origem do
sofrimento, a cessação do sofrimento e o caminho que conduz à cessação do
sofrimento. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que,
bhikkhus, é sofrimento? Deve ser dito: os cinco agregados influenciados pelo
apego. Quais cinco? O agregado da forma influenciado pelo apego, o agregado da
sensação influenciado pelo apego, o agregado da percepção influenciado pelo
apego, o agregado das formações volitivas influenciado pelo apego, o agregado da
consciência influenciado pelo apego. Isso é chamado sofrimento.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é a origem do sofrimento? É o desejo que conduz a uma renovada existência, acompanhado 
pela cobiça e pelo prazer, buscando o prazer aqui e ali; isto é, o desejo pelos prazeres sensuais, 
o desejo por ser/existir, o desejo por não ser/existir.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é a cessação do sofrimento? É o desaparecimento e cessação sem deixar vestígios 
daquele mesmo desejo, abrir mão, descartar, libertar-se, despegar desse mesmo desejo. Isso é chamado a cessação do
sofrimento.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é o caminho que conduz à cessação do sofrimento? É este Nobre Caminho
Óctuplo; isto é, entendimento correto ... concentração correta. Isso é chamado
o caminho que conduz à cessação do sofrimento.”<b style='mso-bidi-font-weight:
normal'><o:p></o:p></b></span></p>


