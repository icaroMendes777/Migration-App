<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'> Em Savatthi. Lá o Abençoado disse o seguinte: “Bhikkhus, se alguém fosse dizer que alguma coisa é ‘um montão daquilo que é prejudicial,’ é acerca dos cinco obstáculos que, falando o que é certo, isso deveria ser dito. Pois esse é um montão completo daquilo que é prejudicial, isto é, os cinco obstáculos. Quais cinco? O obstáculo do desejo sensual, o obstáculo da má vontade, o obstáculo da preguiça e torpor, o obstáculo da inquietação e ansiedade, o obstáculo da dúvida. Se alguém fosse dizer que alguma coisa é ‘um montão daquilo que é prejudicial,’ é acerca dos cinco obstáculos que, falando o que é certo, isso deveria ser dito. Pois esse é um montão completo daquilo que é prejudicial, isto é, os cinco obstáculos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'> “Bhikkhus, se alguém fosse dizer que alguma coisa é ‘um montão daquilo que é benéfico,’ é acerca dos quatro fundamentos da atenção plena que, falando o que é certo, isso deveria ser dito. Pois esse é um montão completo daquilo que é benéfico, isto é, os quatro fundamentos da atenção plena. Quais quatro? Aqui, bhikkhus, um bhikkhu permanece contemplando o corpo como um corpo, ardente, plenamente consciente e com atenção plena, tendo colocado de lado a cobiça e o desprazer pelo mundo. Ele permanece contemplando as sensações como sensações ... mente como mente ... objetos mentais como objetos mentais, ardente, plenamente consciente e com atenção plena, tendo colocado de lado a cobiça e o desprazer pelo mundo. Se alguém fosse dizer que alguma coisa é ‘um montão daquilo que é benéfico,’ é acerca dos quatro fundamentos da atenção plena que, falando o que é certo, isso deveria ser dito. Pois esse é um montão completo daquilo que é benéfico, isto é, os quatro fundamentos da atenção plena.”<o:p></o:p></span></p>





