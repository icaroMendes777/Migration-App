<p class=Normal>Certa ocasião o ven. Sariputta estava em Magadha, no vilarejo Nalaka.[1] Nessa ocasião o errante asceta Samandakani foi até ele e perguntou: </p>

<p class=Normal> “Amigo Sariputta, o que é a felicidade e o que é o sofrimento?” </p>

<p class=Normal> “Renascer, amigo, é sofrimento; não renascer é a felicidade.” </p>

<p class=Normal> “Quando há o renascimento este sofrimento será encontrado: calor e frio; fome e sede; evacuar e urinar; contato com o fogo, a vara e a espada; até mesmo os próprios parentes e amigos estando juntos abusam dele. Quando há renascimento, esse tipo de sofrimento será encontrado.” </p>

<p class=Normal> “Quando não há o renascimento este sofrimento não será encontrado: calor e frio; fome e sede; evacuar e urinar; contato com o fogo, a vara e a espada; nem os próprios parentes e amigos estando juntos abusam dele. Quando não há renascimento, esse tipo de sofrimento não será encontrado.”



