<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
quando um nobre discípulo possui quatro coisas, os devas se regozijam e falam
da semelhança dele [para com eles, devas]. Quais quatro?<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
bhikkhus, o nobre discípulo possui perfeita claridade, serenidade e confiança no Buda assim: ‘O Abençoado é um arahant,
perfeitamente iluminado, consumado no verdadeiro conhecimento e conduta,
bem-aventurado, conhecedor dos mundos, um líder insuperável de pessoas
preparadas para serem treinadas, mestre de devas e humanos, desperto,
sublime.’ Para aquelas devatas que faleceram aqui
[no mundo humano] e renasceram lá [no mundo celestial] possuindo a convicção
comprovada no Buda, o pensamento ocorre: ‘Como o nobre discípulo possui a mesma
perfeita claridade, serenidade e confiança no Buda, virtudes que possuíamos quando falecemos lá e
renascemos aqui, ele virá para a presença dos devas.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Outra vez,
bhikkhus, o nobre discípulo possui perfeita claridade, serenidade e confiança no Dhamma ... na Sangha
... Ele possui as virtudes apreciadas pelos nobres – intactas, não-laceradas, imaculadas,
não-matizadas, libertadoras, elogiadas pelos sábios, desapegadas, que conduzem
à concentração. Para aquelas devatas que faleceram aqui [no mundo humano] e
renasceram lá [no mundo celestial] possuindo as virtudes apreciadas pelos
nobres, o pensamento ocorre: ‘Como o nobre discípulo possui o mesmo tipo de
virtudes apreciadas pelos nobres, virtudes que possuíamos quando falecemos lá e
renascemos aqui, ele virá para a presença dos devas.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando,
bhikkhus, um nobre discípulo possui essas quatro coisas, os devas se regozijam
e falam da semelhança dele [para com eles, devas].”<o:p></o:p></span></p>

