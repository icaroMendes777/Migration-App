<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. Agora, naquela ocasião o Abençoado estava instruindo, motivando,
estimulando e encorajando os bhikkhus com um discurso do Dhamma a respeito dos
cinco agregados influenciados pelo apego. E aqueles bhikkhus estavam ouvindo o
Dhamma com ouvidos ávidos, ocupando-se com isso como um assunto de vital
importância, aplicando toda a mente nisso.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então,
ocorreu a Mara, o Senhor do Mal: “Esse contemplativo Gotama está instruindo,
motivando, estimulando e encorajando os bhikkhus .., que estão aplicando toda a
mente nisso. Vou me aproximar do contemplativo Gotama para confundí-los.”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Agora,
naquela ocasião, uma grande quantidade de tigelas de esmolar alimentos tinham
sido colocadas do lado de fora. Então, Mara o Senhor do Mal, se manifestou em
forma de boi e se aproximou daquelas tigelas. Então, um bhikkhu disse para
outro: “Bhikkhu, bhikkhu! Aquele boi poderá quebrar as tigelas.” Quando isso
foi dito, o Abençoado disse para aquele bhikkhu: “Aquele não é um boi, bhikkhu.
Aquele é Mara, o Senhor do Mal, que veio para confundí-los.” <span
style='mso-tab-count:1'>          </span><o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, o
Abençoado, compreendendo que, “Esse é Mara o Senhor do Mal, se dirigiu a Mara
em versos:<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Forma, sensação e percepção, <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>consciência e formações -<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>‘eu não sou isso, isso não é meu,’<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>assim a pessoa se desapega. <o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Embora eles a procurem por todos lados,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Mara e seu exército não a encontram:<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>a pessoa assim desapegada, segura,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>que superou todos grilhões.” [<a href="#N1">1</a>]<o:p></o:p></span></p>


<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Então, Mara, o Senhor do Mal, compreendendo que, “O Abençoado sabe quem
sou, O Iluminado sabe quem sou” triste e desapontado, desapareceu no mesmo
instante.<o:p></o:p></span></p>


