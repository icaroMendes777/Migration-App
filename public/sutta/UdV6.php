<p class=Normal>Assim ouvi. Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no Parque de Anathapindika.</p>



<p class=Normal>Agora, naquela ocasião o venerável Maha Kaccana vivia entre o povo de Avanti na Montanha de Pavatta perto da cidade de Kuraraghara. E naquele momento o discípulo leigo Sona Kotikanna era o patrono do venerável Maha Kaccana. </p>



<p class=Normal>Então, enquanto Sona Kotikanna estava só, recluso, o seguinte pensamento lhe ocorreu: "De acordo com o Dhamma que o mestre Maha Kaccana ensina, não é fácil viver em casa e praticar a vida santa completamente perfeita, totalmente pura, como uma concha polida. E se eu raspasse o meu cabelo e barba, vestisse os mantos de cor ocre e seguisse a vida santa?”</p>



<p class=Normal>Ele foi então até o venerável Maha Kaccana e ao chegar, tendo o homenageado, ele se sentou a um lado. Estando sentado ali Sona disse ao venerável Maha Kaccana: "Há pouco, venerável senhor, enquanto eu estava só, recluso, o seguinte pensamento me ocorreu: "De acordo com o Dhamma que o mestre Maha Kaccana ensina, não é fácil viver em casa e praticar a vida santa completamente perfeita, totalmente pura, como uma concha polida. E se eu raspasse o meu cabelo e barba, vestisse os mantos de cor ocre e seguisse a vida santa?" Dê-me a admissão na vida santa, mestre Maha Kaccayana!”</p>



<p class=Normal>Quando isto foi dito, o venerável Maha Kaccana disse para Sona Kotikanna: "Difícil é, Sona, assumir por toda a vida os votos da vida santa: o celibato, o leito solitário e o voto de não comer mais do que uma refeição ao dia." E então abrandou-se em Sona Kotikanna a ideia de seguir a vida santa.</p>



<p class=Normal>Então, uma segunda vez, enquanto Sona Kotikanna estava só, recluso, o seguinte pensamento lhe ocorreu: "De acordo com o Dhamma que o mestre Maha Kaccana ensina, não é fácil viver em casa e praticar a vida santa completamente perfeita, totalmente pura, como uma concha polida. E se eu raspasse o meu cabelo e barba, vestisse os mantos de cor ocre e seguisse a vida santa?”</p>



<p class=Normal>Ele foi então até o venerável Maha Kaccana e ao chegar, tendo o homenageado, ele se sentou a um lado. Estando sentado ali Sona disse ao venerável Maha Kaccana: "Há pouco, venerável senhor, enquanto eu estava só, recluso, o seguinte pensamento me ocorreu: "De acordo com o Dhamma que o mestre Maha Kaccana ensina, não é fácil viver em casa e praticar a vida santa completamente perfeita, totalmente pura, como uma concha polida. E se eu raspasse o meu cabelo e barba, vestisse os mantos de cor ocre e seguisse a vida santa?" Dê-me a admissão na vida santa, mestre Maha Kaccana!”</p>



<p class=Normal>Quando isto foi dito, o venerável Maha Kaccana disse para Sona Kotikanna: "Difícil é, Sona, assumir por toda a vida os votos da vida santa: o celibato, o leito solitário e o voto de não comer mais do que uma refeição ao dia." E então, uma segunda vez, abrandou-se em Sona Kotikanna a ideia de seguir a vida santa.</p>



<p class=Normal>Então, uma terceira vez, enquanto Sona Kotikanna estava só, recluso, o seguinte pensamento lhe ocorreu: "De acordo com o Dhamma que o mestre Maha Kaccana ensina, não é fácil viver em casa e praticar a vida santa completamente perfeita, totalmente pura, como uma concha polida. E se eu raspasse o meu cabelo e barba, vestisse os mantos de cor ocre e seguisse a vida santa?”</p>



<p class=Normal>Ele foi então até o venerável Maha Kaccana e ao chegar, tendo o homenageado, ele se sentou a um lado. Estando sentado ali Sona disse ao venerável Maha Kaccana: "Há pouco, venerável senhor, enquanto eu estava só, recluso, o seguinte pensamento me ocorreu: "De acordo com o Dhamma que o mestre Maha Kaccana ensina, não é fácil viver em casa e praticar a vida santa completamente perfeita, totalmente pura, como uma concha polida. E se eu raspasse o meu cabelo e barba, vestisse os mantos de cor ocre e seguisse a vida santa?" Dê-me a admissão na vida santa, mestre Maha Kaccana!”</p>



<p class=Normal>Então o venerável Maha Kaccana deu ao Sona Kotikanna a admissão na vida santa.</p>



<p class=Normal>Agora, naquela ocasião, havia poucos monges residindo na região sul de Avanti. Então, foi só apenas depois de três anos - tendo reunido daqui e dali, com dificuldade e complicações, o quórum mínimo de dez bhikkhus - que o venerável Maha Kaccana deu ao venerável Sona a admissão completa. Então, foi após o venerável Sona ter completado o retiro das chuvas, enquanto ele se encontrava só e recluso, que lhe ocorreu o seguinte pensamento: "Eu nunca estive cara a cara com o Abençoado. Eu apenas ouço dizer que ele é desse e daquele jeito. Se meu preceptor me desse permissão, eu iria até o Abençoado, um arahant, perfeitamente iluminado.”</p>



<p class=Normal>Então, após se levantar da meditação ao anoitecer, ele foi até o venerável Maha Kaccana. Depois de cumprimentá-lo, ele sentou a um lado e disse ao venerável Maha Kaccana: "Há pouco, venerável senhor, enquanto eu estava só, recluso, o seguinte pensamento me ocorreu: "Eu nunca estive cara a cara com o Abençoado. Eu apenas ouço dizer que ele é desse e daquele jeito. Se meu preceptor me desse permissão, eu iria até o Abençoado, um arahant, perfeitamente iluminado.”</p>



<p class=Normal>"Bom, Sona. Muito bom. Vá, Sona, veja o Abençoado, um arahant, perfeitamente iluminado. Você verá o Abençoado,  sereno e confiante e inspirando serena confiança, com os sentidos em paz, com a mente em paz, tendo alcançado a tranqüilidade e controle supremos, treinado, protegido, as faculdades dos sentidos controladas, um Naga. Ao vê-lo, em meu nome o homenageie com a cabeça aos seus pés, e pergunte se ele está livre de enfermidades e aflições, se está com saúde, forte e vivendo com conforto, dizendo: "Meu preceptor, senhor, o homenageia com a sua cabeça aos seus pés e pergunta se o senhor se encontra livre de enfermidades e aflições, se está com saúde, forte e vivendo com conforto.”</p>



<p class=Normal>Dizendo, "Sim, venerável senhor", o venerável Sona - satisfeito e aprovando as palavras do venerável Maha Kaccana - levantou-se do seu assento e, depois de homenageá-lo, mantendo-o à sua direita, arrumou a sua habitação, tomou a tigela e o manto externo e saiu caminhando rumo a Savatthi. Caminhando em etapas, ele chegou em Savatthi no Bosque de Jeta, no Parque de Anathapindika. Ele foi até o Abençoado e ao chegar, tendo o homenageado, ele sentou a um lado. Sentado, ele disse ao Abençoado, "Senhor, meu preceptor, venerável Maha Kaccana, o homenageia com a sua cabeça aos pés do Abençoado e pergunta se o senhor se encontra livre de enfermidades e aflições, se está com saúde, forte e vivendo com conforto.”</p>



<p class=Normal>"Você está bem, bhikkhu? Você está saudável? Você percorreu sua jornada sem grande cansaço? Você pôde esmolar alimento sem grande dificuldade?”</p>



<p class=Normal>"Abençoado, eu estou bem. Abençoado, eu estou saudável. Abençoado, eu percorri a minha jornada sem grande cansaço, e pude esmolar alimento sem grande dificuldade.”</p>



<p class=Normal>Então o Abençoado se dirigiu ao venerável Ananda, dizendo: "Ananda, prepare alojamento para este bhikkhu visitante.”</p>



<p class=Normal>Agora o pensamento ocorreu ao venerável Ananda: "Quando o Abençoado me ordena "Ananda, prepare alojamento para este bhikkhu visitante", ele intenciona dividir alojamento com aquele bhikkhu. O Abençoado intenciona dividir alojamento com o venerável Sona." Então ele preparou alojamento para o venerável Sona no mesmo local onde o Abençoado estava instalado. Então o Abençoado, após ter passado a maior parte da noite ao ar livre, lavou seus pés e entrou no alojamento. Igualmente, o venerável Sona, após ter passado a maior parte da noite ao ar livre, lavou seus pés e entrou no alojamento. Então, ao fim da noite, ao levantar-se, o Abençoado chamou o venerável Sona, dizendo "Bhikkhu, gostaria que você recitasse o Dhamma.”</p>



<p class=Normal>Respondendo, "Sim, venerável senhor.", o venerável Sona recitou todas os dezesseis discursos do Atthaka Vagga. Ao fim da recitação pelo venerável Sona, o Abençoado expressou sua aprovação: "Bom, bhikkhu. Muito bom. Você aprendeu bem os versos do Atthaka Vagga, você os contemplou bem, você os manteve bem em mente. Você os apresenta bem, com clareza e de modo imaculado, impecável, tornando o significado inteligível. Quantos retiros das chuvas você completou?”</p>



<p class=Normal>"Abençoado, eu completei um retiro das chuvas.”</p>



<p class=Normal>"Mas por qual razão você esperou tanto tempo para ter a admissão na vida santa?”</p>



<p class=Normal>"Por muito tempo, senhor, eu vi as desvantagens dos prazeres sensuais, mas a vida em família é confinada por vários deveres, muitas coisas a serem feitas.”</p>



<p class=Normal>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p> 



	<p class=Normal style='margin-left:.75in'>"Vendo as desvantagens do mundo,<br>

	sabendo do estado livre de aquisições,<br>

	aquele que é nobre não se deleita no mal,<br>

	não encontra felicidade no mal.”</p>

