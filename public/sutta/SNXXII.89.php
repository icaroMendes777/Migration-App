<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em certa
ocasião, muitos monges sêniores estavam em Kosambi no Parque de Ghosita. E
naquela ocasião o venerável Khemaka estava no Parque da Árvore de Jujuba, enfermo,
com dores, gravemente doente. Então, no final da tarde os monges sêniores
saíram do seu isolamento e se dirigiram ao venerável Dasaka dizendo, “Venha, amigo
Dasaka. Vá até o bhikkhu Khemaka e ao chegar diga: ‘Os bhikkhus sêniores, amigo
Khemaka, lhe dizem: “Esperamos que você esteja melhorando, amigo. Esperamos que
você se sinta confortável. Esperamos que as suas dores estejam diminuindo e não
aumentando. Esperamos que haja sinais da sua diminuição e não do seu aumento.”’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Respondendo,
“Assim seja, amigos,” para os monges sêniores, o venerável Dasaka foi até o venerável
Khemaka e ao chegar disse: “Os bhikkhus sêniores, amigo Khemaka, lhe dizem:
‘Esperamos que você esteja melhorando, amigo. Esperamos que você se sinta
confortável. Esperamos que as suas dores estejam diminuindo e não aumentando.
Esperamos que haja sinais da sua diminuição e não do seu aumento.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu não
estou melhorando, meu amigo. Eu não me sinto confortável. Minhas dores extremas
estão aumentando, não diminuindo. Existem sinais do seu aumento e não de
diminuição.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o
venerável Dasaka foi até os bhikkhus sêniores e, ao chegar, disse, “O bhikkhu
Khemaka disse: ‘Eu não estou melhorando, meu amigo. Eu não me sinto
confortável. Minhas dores extremas estão aumentando, não diminuindo. Existem
sinais do seu aumento e não de diminuição.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venha,
amigo Dasaka, vá até o bhikkhu Khemaka e ao chegar diga: ‘Os bhikkhus sêniores,
amigo Khemaka, lhe dizem: “Com respeito aos cinco agregados influenciados pelo apego descritos pelo
Abençoado, isto é, a forma como um agregado influenciado pelo apego, a sensação
como um agregado influenciado pelo apego, a percepção como um agregado
influenciado pelo apego, as formações volitivas como um agregado influenciado pelo apego,
a consciência como um agregado influenciado pelo apego; você supõe que alguma
coisa relacionada com esses cinco agregados influenciado pelo apego sejam o eu,
ou pertençam ao eu?””<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Respondendo,
“Assim seja, amigos,” aos bhikkhus sêniores, o venerável Dasaka foi até o venerável Khemaka
e ao chegar disse: “Os bhikkhus sêniores, amigo Khemaka, lhe dizem: ‘Com
respeito aos cinco agregados influenciados pelo apego descritos pelo Abençoado, isto é, a forma como um
agregado influenciado pelo apego, a sensação como um agregado influenciado pelo
apego, a percepção como um agregado influenciado pelo apego, as formações volitivas como
um agregado influenciado pelo apego, a consciência como um agregado
influenciado pelo apego; você supõe que alguma coisa relacionada com esses
cinco agregados influenciado pelo apego sejam o eu, ou pertençam ao eu?”’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigo, com
respeito aos cinco agregados influenciados pelo apego descritos pelo Abençoado, isto é, a forma como um
agregado influenciado pelo apego, a sensação como um agregado influenciado pelo
apego, a percepção como um agregado influenciado pelo apego, as formações volitivas como
um agregado influenciado pelo apego, a consciência como um agregado
influenciado pelo apego; não há nada que eu suponha ser o eu ou pertencer ao
eu.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o
venerável Dasaka foi até os bhikkhus sêniores e ao chegar disse: “O bhikkhu Khemaka
me disse: ‘Amigo, com respeito aos cinco agregados influenciados pelo apego descritos pelo Abençoado,
isto é, a forma como um agregado influenciado pelo apego, a sensação como um
agregado influenciado pelo apego, a percepção como um agregado influenciado
pelo apego, as formações volitivas como um agregado influenciado pelo apego, a
consciência como um agregado influenciado pelo apego; não há nada que eu
suponha ser o eu, ou pertencer ao eu.’” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venha,
amigo Dasaka, vá até o bhikkhu Khemaka e ao chegar diga: ‘Com respeito aos
cinco agregados influenciados pelo apego descritos pelo Abençoado, isto é, a forma como um agregado
influenciado pelo apego, a sensação como um agregado influenciado pelo apego, a
percepção como um agregado influenciado pelo apego, as formações volitivas como um
agregado influenciado pelo apego, a consciência como um agregado influenciado
pelo apego: se, com respeito a esses cinco agregados do apego, o venerável Khemaka
supõe que nada seja o eu, ou que pertença ao eu, então o venerável Khemaka é um
arahant, desprovido de impurezas.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigo, com
respeito aos cinco agregados influenciados pelo apego descritos pelo Abençoado, isto é, a forma como um
agregado influenciado pelo apego, a sensação como um agregado influenciado pelo
apego, a percepção como um agregado influenciado pelo apego, as formações volitivas como
um agregado influenciado pelo apego, a consciência como um agregado
influenciado pelo apego: com respeito a esses cinco agregados influenciado pelo
apego, não há nada que eu suponha ser o eu, ou que pertença ao eu, mas no
entanto eu não sou um arahant. Com relação a esses cinco agregados influenciado
pelo apego, ‘Eu sou’ ainda não foi subjugado, embora eu não suponha que ‘eu sou
isso.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o
venerável Dasaka foi até os bhikkhus sêniores e, ao chegar, disse: “O bhikkhu
Khemaka disse: ‘Amigo, com respeito aos cinco agregados influenciados pelo apego descritos pelo Abençoado,
isto é, a forma como um agregado influenciado pelo apego, a sensação como um
agregado influenciado pelo apego, a percepção como um agregado influenciado
pelo apego, as formações volitivas como um agregado influenciado pelo apego, a
consciência como um agregado influenciado pelo apego: com respeito a esses
cinco agregados influenciado pelo apego, não há nada que eu suponha ser o eu,
ou que pertença ao eu, mas no entanto eu não sou um arahant. Com relação a
esses cinco agregados influenciado pelo apego, ‘Eu sou’ ainda não foi
subjugado, embora eu não suponha que ‘eu sou isso’.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venha,
amigo Dasaka, vá até o bhikkhu Khemaka e ao chegar diga: ‘Os bhikkhus sêniores,
amigo Khemaka, lhe dizem: “Amigo Khemaka, esse ‘eu sou’ do qual você fala: o
que você diz que ‘eu sou’? Você diz, ‘Eu sou a forma,’ ou você diz, ‘Eu sou
outra coisa distinta da forma’? Você diz, ‘Eu sou a sensação ... percepção ...
formações volitivas ... consciência,’ ou você diz, ‘Eu sou outra coisa distinta da
consciência’? Esse ‘Eu sou’ do qual você fala: o que você diz que ‘eu sou’?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Respondendo,
“Assim seja, amigos,” aos bhikkhus sêniores, o venerável Dasaka foi até o venerável
Khemaka e ao chegar disse: “Os sêniores, amigo Khemaka, lhe dizem: ‘Amigo
Khemaka, esse “eu sou” do qual você fala: o que você diz que “eu sou”? Você
diz, “Eu sou a forma,” ou você diz, “Eu sou outra coisa distinta da forma”?
Você diz, “Eu sou a sensação ... percepção ... formações volitivas ... consciência,” ou
você diz, “Eu sou outra coisa distinta da consciência”? Esse “Eu sou” do qual
você fala: o que você diz que “eu sou”?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Já basta,
amigo Dasaka. O que é que se consegue com esse correr para cá e para lá? Pegue
a minha bengala. Eu mesmo irei até os bhikkhus sêniores.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o
venerável Khemaka, apoiando-se sobre a bengala, foi até os bhikkhus sêniores e ao
chegar eles se cumprimentaram. </span>Quando a conversa cortês e amigável havia
terminado<span lang=PT-BR style='mso-ansi-language:PT-BR'>, ele sentou a um
lado e os bhikkhus sêniores disseram: “Amigo Khemaka, esse ‘eu sou’ do qual
você fala; o que você diz que ‘eu sou’? Você diz, ‘Eu sou a forma,’ ou você
diz, ‘Eu sou outra coisa distinta da forma’? Você diz, ‘Eu sou a sensação...
percepção... formações volitivas... consciência,’ ou você diz, ‘Eu sou outra coisa
distinta da consciência’? Esse ‘Eu sou’ do qual você fala: o que você diz que
‘eu sou’?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigos,
não é que eu diga ‘Eu sou a forma,’ nem digo ‘Eu sou outra coisa distinta da
forma.’ Não é que eu diga, ‘Eu sou a sensação... percepção... formações volitivas...
consciência,’ nem digo ‘Eu sou outra coisa distinta da consciência.’ Com
relação a esses cinco agregados influenciados pelo apego, ‘Eu sou’ ainda não
foi subjugado, embora eu não suponha que ‘Eu sou isso.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“É como o
perfume de uma flor de lótus azul, vermelha ou branca: Se alguém chamasse isso
de o perfume da pétala ou o perfume da cor ou o perfume do pistilo, estaria ele
falando da forma correta?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
amigo.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então como
ele iria descrevê-lo se fosse descrever da forma correta?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Como o
perfume da flor: assim é como ele o descreveria se fosse descrever da forma
correta.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Da mesma forma,
amigos, não é que eu diga ‘eu sou a forma,’ nem digo ‘Eu sou outra coisa
distinta da forma.’ Não é que eu diga, ‘Eu sou a sensação... percepção...
formações volitivas... consciência,’ nem digo ‘Eu sou outra coisa distinta da
consciência.’ Com relação a esses cinco agregados influenciados pelo apego, ‘Eu
sou’ ainda não foi subjugado, embora eu não suponha que ‘Eu sou isso.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigos,
embora um nobre discípulo tenha abandonado os cinco primeiros grilhões, ele
ainda possui em relação aos cinco agregados influenciados pelo apego a
presunção residual remanescente ‘Eu sou,’ o desejo ‘Eu sou,’ a obsessão ‘Eu
sou.’ Mas em outra ocasião, mais tarde, ele permanece contemplando o fenômeno
da origem e da cessação em relação aos cinco agregados: ‘Assim é a forma, essa
é a sua origem, essa é a sua cessação. Assim é a sensação ... Assim é a
percepção ... Assim são as formações volitivas ... Assim é a consciência, essa é a sua
origem, essa é a sua cessação.’ Ao contemplar a origem e a cessação desses
cinco agregados, a presunção residual remanescente ‘Eu sou,’ o desejo ‘Eu sou,’
a obsessão ‘Eu sou,’ é totalmente destruída.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Como com
um pano sujo e manchado: o seu dono o entrega a uma lavadeira, que o esfrega
com o sal da terra, ou alvejante, ou esterco de vaca, e depois o enxágua com
água limpa. Agora, embora o pano esteja limpo e sem manchas, ele ainda possui
um odor residual remanescente de sal da terra, ou alvejante, ou esterco de
vaca. A lavadeira o retorna ao dono e ele coloca o pano<span
style="mso-spacerun: yes">  </span>num cesto de vime infundido com aromas, o odor
residual remanescente de sal da terra, ou alvejante, ou esterco de vaca é
totalmente destruído.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Da mesma
forma, amigos, embora um nobre discípulo tenha abandonado os cinco primeiros
grilhões, ele ainda possui em relação aos cinco agregados influenciados pelo
apego a presunção residual remanescente ‘Eu sou,’ o desejo ‘Eu sou,’ a obsessão
‘Eu sou.’ Mas em outra ocasião, mais tarde, ele permanece contemplando o
fenômeno da origem e da cessação em relação aos cinco agregados: ‘Assim é a
forma, essa é a sua origem, essa é a sua cessação. Assim é a sensação ... Assim
é a percepção ... Assim são as formações volitivas ... Assim é a consciência, essa é a
sua origem, essa é a sua cessação.’ Ao contemplar a origem e a cessação desses
cinco agregados, a presunção residual remanescente ‘Eu sou,’ o desejo ‘Eu sou,’
a obsessão ‘Eu sou,’ é totalmente destruída.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isso
foi dito, os bhikkhus sêniores disseram ao venerável Khemaka, “Nós não o
interrogamos, venerável Khemaka, com o propósito de criar-lhe problemas, apenas pensamos
que o venerável Khemaka é capaz de declarar a mensagem do Abençoado, ensiná-la,
descrevê-la, expô-la, revelá-la, explicá-la, torná-la simples – tal como ele de
fato declarou, ensinou, descreveu, expôs, revelou, explicou e tornou-a
simples.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o
que o venerável Khemaka disse. Os bhikkhus sêniores ficaram satisfeitos e contentes
com as palavras do venerável Khemaka. E enquanto essa explicação estava sendo dada,
as mentes de cerca de sessenta bhikkhus, através do desapego, libertaram-se
totalmente das impurezas – bem como a mente do venerável Khemaka.<o:p></o:p></span></p>


