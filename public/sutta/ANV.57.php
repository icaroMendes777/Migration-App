<p class=Normal><a name=R1></a>
&quot;Há esses cinco fatos que devem ser refletidos [<a href="#N1">1</a>] por todos com
freqüência, quer seja mulher ou homem, leigo ou ordenado. Quais cinco? <o:p></o:p></span></p>


<p class=Normal>
&quot;'Eu estou sujeito ao envelhecimento, não superei o
envelhecimento.’ Esse é o primeiro fato que deve ser refletido por todos com
freqüência, quer seja mulher ou homem, leigo ou ordenado.<o:p></o:p></span></p>


<p class=Normal>
&quot;'Eu estou sujeito à enfermidade, não superei a enfermidade'. Esse é o segundo fato ... <o:p></o:p></span></p>


<p class=Normal>
&quot;'Eu estou sujeito à morte, não superei a morte'. Esse é o terceiro fato ... <o:p></o:p></span></p>


<p class=Normal>
&quot;'Eu me tornarei diferente, separado de tudo o que é querido e
amado'. Esse é o quarto fato ... <o:p></o:p></span></p>


<p class=Normal>
&quot;'Eu sou o dono das minhas ações (kamma), herdeiro das minhas
ações, nascido das minhas ações, relacionado através das minhas ações e tenho
as minhas ações como árbitro. O que quer que eu faça, para o bem ou para o mal,
disso me tornarei o herdeiro'. Esse é o quinto fato ... <o:p></o:p></span></p>


<p class=Normal>
&quot;Esses são os cinco fatos que devem ser refletidos por todos com
freqüência, quer seja mulher ou homem, leigo ou ordenado. <o:p></o:p></span></p>


<p class=Normal>
&quot;Agora, baseado em que linha de raciocínio alguém deve<span
style="mso-spacerun: yes">  </span>refletir com freqüência que 'Eu estou
sujeito ao envelhecimento, não superei o envelhecimento'? Há seres que estão
embriagados com a [típica] embriaguez dos jovens com a juventude. Por causa
dessa embriaguez com a juventude, eles se conduzem de uma forma prejudicial com
o corpo ... com a linguagem ... com a mente. Porém, ao refletir sobre isso com
frequência, essa embriaguez dos jovens com a juventude ou é
totalmente abandonada ou é enfraquecida. <o:p></o:p></span></p>


<p class=Normal>
&quot;Agora, baseado em que linha de raciocínio alguém deve<span
style="mso-spacerun: yes">  </span>refletir com freqüência que 'Eu estou
sujeito à enfermidade, não superei a enfermidade'? Há seres que estão
embriagados com a [típica] embriaguez das pessoas saudáveis com a saúde. Por
causa dessa embriaguez com a saúde, eles se conduzem de uma forma prejudicial
com o corpo ... com a linguagem ... com a mente. Porém, ao refletir sobre isso com
frequência, essa embriaguez das pessoas saudáveis com a saúde
ou é totalmente abandonada ou é enfraquecida. <o:p></o:p></span></p>


<p class=Normal>
&quot;Agora, baseado em que linha de raciocínio alguém deve<span
style="mso-spacerun: yes">  </span>refletir com freqüência que 'Eu estou
sujeito a morte, não superei a morte'? Há seres que estão embriagados com a
[típica] embriaguez das pessoas com a vida. Por causa dessa embriaguez com a
vida, eles se conduzem de uma forma prejudicial com o corpo ... com a linguagem
... com a mente. Porém, ao refletir sobre isso com frequência, essa
embriaguez das pessoas com a vida ou é totalmente abandonada ou é
enfraquecida. <o:p></o:p></span></p>


<p class=Normal>
&quot;Agora, baseado em que linha de raciocínio alguém deve<span
style="mso-spacerun: yes">  </span>refletir com freqüência que 'Eu me
tornarei diferente, separado de tudo<span style="mso-spacerun: yes">  </span>o
que é querido e amado'? Há seres que sentem cobiça e desejo pelas coisas
que consideram queridas e amadas. Por causa dessa cobiça e desejo, eles se
conduzem de uma forma prejudicial com o corpo ... com a linguagem ... com a
mente. Porém, ao refletir sobre isso com frequência, esse desejo e
cobiça pelas coisas que são queridas e amadas ou é totalmente abandonado ou
é enfraquecido. <o:p></o:p></span></p>


<p class=Normal>&quot;Agora, baseado em que linha de raciocínio alguém
deve<span style='mso-spacerun:yes'>  </span>refletir com freqüência que
'Eu sou o dono das minhas ações (kamma), herdeiro das minhas ações, nascido das
minhas ações, relacionado através das minhas ações e tenho as minhas ações como
árbitro. O que quer que eu faça, para o bem ou para o mal, disso me tornarei
herdeiro'? Há seres que se conduzem de uma forma prejudicial com o corpo ...
com a linguagem ... com a mente. Porém, ao refletir sobre isso com freqüência, essa conduta prejudicial com o corpo, linguagem, e mente ou é
totalmente abandonada ou é enfraquecida. <o:p></o:p></span></span></span></p>


<p class=Normal>
&quot;Agora, um nobre discípulo considera o seguinte: ‘Eu não sou o
único sujeito ao envelhecimento, que não superou o envelhecimento, mas sempre
que há seres, indo e vindo, falecendo e renascendo, todos esses seres estão
sujeitos ao envelhecimento, não superaram o envelhecimento.' Ao refletir isso com
frequência, os [fatores do] caminho surgem. Ele permanece fiel ao
caminho, desenvolvendo-o e cultivando-o. Ao permanecer fiel ao caminho,
desenvolvendo-o e cultivando-o, os grilhões são abandonados, as tendências
subjacentes são destruídas. <o:p></o:p></span></p>


<p class=Normal>
&quot;Além disso, um nobre discípulo considera o seguinte: ‘Eu não sou
o único sujeito à enfermidade, que não superou a enfermidade’ ... ”Eu não sou o
único sujeito à morte, que não superou a morte’ ... ’Eu não sou o único que me
tornarei diferente, separado de tudo que é querido e amado’ ... <o:p></o:p></span></p>


<p class=Normal>&quot;Um nobre discípulo considera o seguinte: 'Eu não
sou o único dono das minhas ações (kamma), herdeiro das minhas ações,
nascido das minhas ações, relacionado através das minhas ações, tendo as
minhas ações como árbitro; que - o que quer que eu faça, para o bem ou para o
mal, disso me tornarei herdeiro,' mas sempre que houver seres, indo e vindo,
falecendo e renascendo, todos esses seres serão os donos das suas ações,
herdeiros das suas ações, nascidos das suas ações, relacionados através das
suas ações e tendo as suas ações como árbitro. O quer que façam, para o bem ou
para o mal, disso eles se tornarão herdeiros.’ Ao refletir isso com freqüência, os [fatores do] caminho surgem.
Ele permanece fiel ao caminho, desenvolvendo-o, cultivando-o. Ao permanecer fiel ao
caminho,<span style='mso-spacerun:yes'>  </span>desenvolvendo-o e cultivando-o,
os grilhões são abandonados, as tendências subjacentes são destruídas.” <o:p></o:p></span></span></span></p>

<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR>Sujeito ao nascimento, sujeito ao
envelhecimento,<br>
sujeito à morte,<br>
pessoas comuns<br>
sentem repulsa por aqueles que sofrem<br>
 com aquilo a que elas mesmas estão sujeitas.<br>
E se eu sentisse repulsa<br>
por seres sujeitos a essas coisas,<br>
não seria digno,<br>
vivendo como eles vivem. </span></p>

<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR>Enquanto mantinha essa atitude - <br>
conhecendo o Dhamma<br>
sem aquisições - <br>
Eu superei toda embriaguez<br>
com a saúde, juventude, e<span style="mso-spacerun: yes">  </span>vida,<br>
como aquele que vê<br>
a renúncia como<span style="mso-spacerun: yes">  </span>segurança.</span></p>

<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR>Em mim, a energia foi estimulada,<br>
A Libertação foi vista claramente.<br>
De nenhum modo<br>
 eu desfrutaria de prazeres dos sentidos.<br>
Tendo seguido a vida santa,<br>
não retornarei. </span></p>

