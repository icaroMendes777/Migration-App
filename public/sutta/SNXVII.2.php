<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, terríveis são o ganho, a honraria e fama, amargos, vis,
obstruem a realização da insuperável segurança contra o cativeiro. Suponham que
um pescador lançasse um anzol com uma isca num lago profundo e um peixe em
busca de comida engolisse o anzol com a isca. Aquele peixe, ao engolir o anzol
do pescador deu de encontro com a calamidade e o desastre, e o pescador poderá
fazer o que quiser com ele. <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’Pescador,’
bhikkhus, é uma designação para Mara, o Senhor do Mal. ‘Anzol com isca’, é uma
designação para o ganho, honraria e fama. Qualquer bhikkhu que saboreie e
desfrute do ganho, honraria e fama é chamado o bhikkhu que engoliu o anzol
com a isca, que deu de encontro com a calamidade e o desastre, e o Senhor do
Mal poderá fazer o que quiser com ele. Tão terríveis, bhikkhus, são o ganho, a
honraria e fama, amargos, vis, obstruem a realização da insuperável
segurança contra o cativeiro. Portanto, bhikkhus, vocês devem praticar da
seguinte forma: ‘Nós abandonaremos o ganho, a honraria e fama que já
surgiram e não permitiremos que o ganho, a honraria e fama que já surgiram
persistam obcecando as nossas mentes.’ Assim vocês deveriam praticar.”<b
style='mso-bidi-font-weight:normal'><o:p></o:p></b></span></p>


