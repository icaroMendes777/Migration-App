<p class=Normal> Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no Parque de Anathapindika. Lá ele se dirigiu aos monges desta forma: “Bhikkhus” – “Venerável Senhor,” eles responderam. O Abençoado disse o seguinte: </p>

<p class=Normal> “Bhikkhus, permaneçam possuídos pela virtude, possuídos pelo Patimokkha, contidos pelas regras do Patimokkha, sejam perfeitos na conduta e na sua esfera de atividades, temendo a menor falha, treinem adotando os preceitos de virtude. </p>

<p class=Normal> (1) “Se um bhikkhu desejar: ‘Que eu seja querido e agradável para os meus companheiros na vida santa, respeitado e benquisto por eles,’ que ele cumpra os preceitos, seja dedicado à tranqüilidade da mente, não negligencie a meditação que conduz aos jhanas, cultive o insight e habite cabanas vazias. </p>

<p class=Normal> (2) “Se um bhikkhu desejar: ‘Que eu obtenha mantos, comida esmolada, moradia e medicamentos,’ que ele cumpra os preceitos ... </p>

<p class=Normal> (3) “Se um bhikkhu desejar: ‘Que as oferendas daqueles cujos mantos, comida esmolada, moradia e medicamentos eu uso, lhes traga grandes frutos e benefícios,’ que ele cumpra os preceitos ... </p>

<p class=Normal> (4) “Se um bhikkhu desejar: ‘Quando os meus pares e parentes, que faleceram, se recordarem de mim com confiança nas suas mentes, que isso lhes traga grandes frutos e grandes benefícios,’ que ele cumpra os preceitos ... </p>

<p class=Normal> (5) “Se um bhikkhu desejar: 'Que eu fique satisfeito com qualquer tipo de manto, comida esmolada, moradia, e medicamentos,' que ele cumpra os preceitos ... </p>

<p class=Normal> (6) “Se um bhikkhu desejar: 'Que eu pacientemente suporte o calor, a fome e a sede; o contato com moscas, mosquitos, vento, sol e criaturas rastejantes; linguagem rude e ofensiva; suporte sensações corporais que são dolorosas, torturantes, penetrantes, cortantes, desagradáveis, agonizantes e que ameaçam a vida,' que ele cumpra os preceitos ... </p>

<p class=Normal> (7) “Se um bhikkhu desejar: ‘Que eu me torne um conquistador do descontentamento e do deleite, e que o descontentamento e o deleite não me conquistem; que eu permaneça transcendendo o descontentamento e o deleite sempre que estes surgirem,’ que ele cumpra os preceitos ... </p>

<p class=Normal> (8) “Se um bhikkhu desejar: ‘Que eu me torne um conquistador do medo e do terror, e que o medo e o terror não me conquistem; que eu permaneça transcendendo o medo e o terror sempre que estes surgirem,’que ele cumpra os preceitos ... </p>

<p class=Normal> (9) “Se um bhikkhu desejar: ‘Que eu me torne um daqueles que obtém de acordo com a vontade, sem problemas ou dificuldades, os quatro jhanas que constituem a mente superior e que proporcionam uma estada prazerosa aqui e agora,’ que ele cumpra os preceitos ... </p>

<p class=Normal> (10) “Se um bhikkhu desejar: ‘Que eu, compreendendo por mim mesmo com conhecimento direto, aqui e agora, entre e permaneça na libertação da mente e na libertação através da sabedoria, que são imaculadas, com a destruição de todas as impurezas,’ que ele cumpra os preceitos, seja dedicado à tranqüilidade da mente, não negligencie a meditação que conduz aos jhanas, cultive o insight e habite cabanas vazias. </p>

<p class=Normal>"Quando foi dito: 'Bhikkhus, permaneçam possuídos pela virtude, possuídos pelo Patimokkha, contidos pelas regras do Patimokkha, sejam perfeitos na conduta e na sua esfera de atividades, temendo a menor falha, treinem adotando os preceitos de virtude,' é por isso que isso foi dito."
</p>


