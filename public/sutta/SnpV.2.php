<p class=Normal><b>Notas:</b></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O <a href="ANVI.61.php">AN VI.61</a>
relata uma discussão entre vários bhikkhus seniores sobre o significado neste
sutta de “ambos lados” e “no meio.” Seis bhikkhus manifestam as seguintes
opiniões distintas: <o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>
- Contato é o
primeiro lado, a origem do contato o segundo lado e a cessação do contato está
no meio.<br>


- O passado é
o primeiro lado, o futuro o segundo lado e o presente está no meio <br>

- A sensação
prazerosa é o primeiro lado, a sensação dolorosa o segundo lado e a sensação
nem prazerosa, nem dolorosa está no meio. <br>

- Mentalidade (nome)
é o primeiro lado, materialidade (forma) o segundo lado e a consciência está no meio. <br>

- As seis
bases externas é o primeiro lado, as seis bases internas o segundo lado e a
consciência está no meio. <br>

- A
identidade é o primeiro lado, a origem da identidade o segundo lado e a cessação
da identidade está no meio. <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O assunto
foi levado até o Buda, que afirma serem as seis interpretações corretas, mas a
interpretação que ele tinha em mente ao responder à pergunta era a primeira. <o:p></o:p></span></p>

<p class=Normal>O sutta descreve o "pregador" como o desejo "pois o desejo prega à produção deste ou daquele estado de ser/existir."</p>









