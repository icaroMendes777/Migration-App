<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim ouvi.
Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no Parque de
Anathapindika. </span>Lá ele se dirigiu aos monges desta forma: “Bhikkhus.” –
“Venerável Senhor,” eles responderam. O Abençoado disse o seguinte:</p>



<p class=Normal>2. “Bhikkhus, quaisquer medos que surjam, todos surgem por
causa do homem tolo, não por causa do homem sábio; quaisquer problemas que
surjam, todos surgem por causa do homem tolo, não por causa do homem sábio;
quaisquer calamidades que surjam, todas surgem por causa do homem tolo, não por
causa do homem sábio. Assim como um fogo, que começa num galpão feito de palha,
queima até mesmo uma casa com um telhado com cumeeira, com as paredes
revestidas por dentro e por fora, cerrada, protegida por barras, com
as janelas fechadas; assim também, bhikkhus, quaisquer medos que surjam ...
todos surgem por causa do homem tolo, não por causa do homem sábio. Assim, o
homem tolo traz o medo, o homem sábio não traz o medo; o homem tolo traz os
problemas, o homem sábio não traz os problemas; o homem tolo traz a calamidade,
o homem sábio não traz a calamidade. Nenhum medo vem do homem sábio, nenhum
problema vem do homem sábio, nenhuma calamidade vem do homem sábio. Portanto,
bhikkhus, assim vocês deveriam praticar: ‘Nós seremos homens sábios, nós
seremos investigadores.’”</p>


<p class=Normal>3. Quando isso foi dito, o venerável Ananda perguntou ao
Abençoado: “De que forma, venerável senhor, pode um bhikkhu ser chamado de
homem sábio e<span style="mso-spacerun: yes">  </span>investigador?”</p>

<p class=Normal>“Quando, Ananda, um bhikkhu tem habilidade com os elementos,
habilidade com as bases, habilidade com a origem dependente, habilidade com
aquilo que é possível e aquilo que é impossível, ele poderá ser chamado de
homem sábio e<span style="mso-spacerun: yes">  </span>investigador.”</p>



<p class=Normal><b>(OS ELEMENTOS)</b></p>



<p class=Normal><a name=T4></a>4. “Mas, venerável senhor, de que forma pode
um bhikkhu ser chamado de hábil com os elementos?”</p>

<p class=Normal>“Existem, Ananda, estes dezoito elementos: o elemento olho,
o elemento forma, o elemento consciência no olho; o elemento ouvido, o elemento
som, o elemento consciência no ouvido; o elemento nariz, o elemento aroma, o
elemento consciência no nariz; o elemento língua, o elemento sabor, o elemento consciência
na língua; o elemento corpo, o elemento tangível, o elemento consciência no
corpo; o elemento mente, o elemento objeto mental, o elemento consciência na
mente. Quando ele conhece e vê esses dezoito elementos, um bhikkhu pode ser
chamado de hábil com os elementos.” [<a href="#N1">1</a>]</p>



<p class=Normal><a name=R1></a>5. “Mas, venerável senhor, pode haver uma
outra maneira pela qual um bhikkhu possa ser chamado de hábil com os
elementos?”</p>

<p class=Normal>“Pode haver, Ananda. Existem, Ananda, estes seis elementos:
o elemento terra, o elemento água, o elemento fogo, o elemento ar, o elemento
espaço e o elemento consciência. Quando ele conhece e vê esses seis elementos,
um bhikkhu pode ser chamado de hábil com os elementos.”</p>



<p class=Normal>6. “Mas, venerável senhor, pode haver uma outra forma pela
qual um bhikkhu possa ser chamado de hábil com os elementos?”</p>

<p class=Normal>“Pode haver, Ananda. Existem, Ananda, estes seis elementos:
o elemento prazer, o elemento dor, o elemento alegria, o elemento tristeza, o
elemento equanimidade e o elemento ignorância. Quando ele conhece e vê esses
seis elementos, um bhikkhu pode ser chamado de hábil com os elementos.”[<a
href="#N2">2</a>]</p>



<p class=Normal><a name=R2></a>7. “Mas, venerável senhor, pode haver uma
outra forma pela qual um bhikkhu possa ser chamado de hábil com os elementos?”</p>

<p class=Normal>“Pode haver, Ananda. Existem, Ananda, estes seis elementos:
o elemento desejo sensual, o elemento renúncia, o elemento má vontade, o
elemento não-má vontade, o elemento crueldade e o elemento não-crueldade.
Quando ele conhece e vê esses seis elementos, um bhikkhu pode ser chamado de
hábil com os elementos.”[<a href="#N3">3</a>]</p>



<p class=Normal><a name=R3></a>8. “Mas, venerável senhor, pode haver uma
outra forma pela qual um bhikkhu possa ser chamado de hábil com os elementos?”</p>

<p class=Normal>“Pode haver, Ananda. Existem, Ananda, estes três elementos:
o elemento sensual, o elemento matéria sutil e o elemento imaterial. Quando ele
conhece e vê esses três elementos, um bhikkhu pode ser chamado de hábil com os
elementos.”[<a href="#N4">4</a>]</p>



<p class=Normal><a name=R4></a>9, “Mas, venerável senhor, pode haver uma
outra forma pela qual um bhikkhu possa ser chamado de hábil com os elementos?”</p>

<p class=Normal>“Pode haver, Ananda. Existem, Ananda, estes dois elementos:
o elemento condicionado e o elemento não condicionado. Quando ele conhece e vê
esses dois elementos, um bhikkhu pode ser chamado de hábil com os elementos.”[<a
href="#N5">5</a>]</p>



<p class=Normal><a name=R5></a><b>(AS BASES)</b></p>



<p class=Normal>10. “Mas, venerável senhor, de que forma pode um bhikkhu ser
chamado de hábil com as bases?”</p>

<p class=Normal>“Existem, Ananda, estas seis bases internas e externas: o
olho e as formas, o ouvido e os sons, o nariz e os aromas, a língua e os
sabores, o corpo e os tangíveis, a mente e os objetos mentais.[<a href="#N6">6</a>]
<a name=R6></a>Quando ele conhece e vê essas seis bases internas e externas, um
bhikkhu pode ser chamado de hábil com as bases.”</p>



<p class=Normal><b>(ORIGEM DEPENDENTE)</b></p>



<p class=Normal><a name=T11></a>11. “Mas, venerável senhor, de que forma
pode um bhikkhu ser chamado de hábil com a origem dependente?”</p>



<p class=Normal>“Aqui, Ananda, um bhikkhu conhece assim: ‘Quando existe
isso, aquilo existe; Com o surgimento disso, aquilo surge. Quando não existe
isso, aquilo também não existe; Com a cessação disto, aquilo cessa. Isto é, com
a ignorância como condição, as formações (surgem); com as formações como
condição, consciência; com a consciência como condição,
mentalidade-materialidade (nome e forma); com a mentalidade-materialidade (nome e forma) como condição, as
seis bases; com as seis bases como condição, contato; com o contato como
condição, sensação; com a sensação como condição, desejo; com o desejo como
condição, apego; com o apego como condição, ser/existir; com o ser/existir como
condição, nascimento; com o nascimento como condição, envelhecimento e morte,
tristeza, lamentação, dor, angústia e desespero. Essa é a origem de toda essa
massa de sofrimento.</p>



<p class=Normal>“’Mas com o desaparecimento e cessação sem deixar vestígios
da ignorância, cessam as formações; com a cessação das formações, cessa a
consciência; com a cessação da consciência, cessa a mentalidade-materialidade (nome e forma);
com a cessação da mentalidade-materialidade (nome e forma), cessam as seis bases; com a
cessação das seis bases, cessa o contato; com a cessação do contato, cessa a
sensação; com a cessação da sensação, cessa o desejo; com a cessação do desejo,
cessa o apego; com a cessação do apego, cessa o ser/existir; com a cessação do
ser/existir, cessa o nascimento; com a cessação do nascimento, envelhecimento e
morte, tristeza, lamentação, dor, angústia e desespero cessam. Essa é a
cessação de toda essa massa de sofrimento.’ Dessa forma, Ananda, um bhikkhu
pode ser chamado de hábil com a origem dependente.”</p>



<p class=Normal><b>(O POSSÍVEL E O IMPOSSÍVEL)</b></p>



<p class=Normal><a name=T12></a>12. “Mas, venerável senhor, de que forma pode um bhikkhu ser
chamado de hábil com aquilo que é possível e aquilo que é impossível?”</p>

<p class=Normal>“Aqui, Ananda, um bhikkhu compreende: ‘É impossível, não
pode acontecer que uma pessoa possuindo o entendimento correto possa tratar
alguma formação como permanente – não existe essa possibilidade.’ [<a href="#N7">7</a>]
<a name=R7></a>E ele compreende: ‘É possível que uma pessoa comum possa tratar
alguma formação como permanente – existe essa possibilidade.’ Ele compreende:
‘É impossível, não pode acontecer que uma pessoa possuindo o entendimento
correto possa tratar alguma formação como prazerosa – não existe essa
possibilidade.’ [<a href="#N8">8</a>] <a name=R8></a>E ele compreende: ‘É
possível que uma pessoa comum possa tratar alguma formação como prazerosa –
existe essa possibilidade.’ Ele compreende: ‘É impossível, não pode acontecer
que uma pessoa possuindo o entendimento correto possa tratar algum fenômeno como o
eu – não existe essa possibilidade.’ E ele compreende: ‘É possível que uma
pessoa comum possa tratar algum fenômeno como o eu – existe essa possibilidade.’[<a
href="#N9">9</a>]</p>



<p class=Normal><a name=R9></a>13. “Ele compreende: ‘É impossível, não pode
acontecer que uma pessoa possuindo o entendimento correto possa tirar a vida da
sua mãe – não existe essa possibilidade.’ [<a href="#N10">10</a>] <a name=R10></a>E
ele compreende: ‘É possível que uma pessoa comum possa tirar a vida da sua mãe
– existe essa possibilidade.’ Ele compreende: ‘É impossível, não pode acontecer
que uma pessoa possuindo o entendimento correto possa tirar a vida do seu pai …
tirar a vida de um arahant – não existe essa possibilidade.’ E ele compreende:
‘É possível que uma pessoa comum possa tirar a vida do seu pai … tirar a vida
de um arahant – existe essa possibilidade.’ Ele compreende: ‘É impossível, não
pode acontecer que uma pessoa possuindo o entendimento correto possa, com a
mente raivosa, derramar o sangue de um Tathagata<span style="mso-spacerun:
yes">  </span>– não existe essa possibilidade.’ E ele compreende: ‘É possível
que uma pessoa comum possa com a mente raivosa, derramar o sangue de um
Tathagata - existe essa possibilidade.’ Ele compreende: ‘É impossível, não pode
acontecer que uma pessoa possuindo o entendimento correto possa causar um cisma
na Sangha … possa reconhecer um outro mestre [<a href="#N11">11</a>] - <a
name=R11></a>não existe essa possibilidade.’ E ele compreende: ‘É possível que
uma pessoa comum possa causar um cisma na Sangha … possa reconhecer um outro
mestre - existe essa possibilidade.’</p>



<p class=Normal>14. “Ele compreende: ‘É impossível, não pode acontecer que
dois Iluminados, Perfeitamente Iluminados, possam surgir ao mesmo tempo em um
mundo - não existe essa possibilidade.’ [<a href="#N12">12</a>] <a name=R12></a>E
ele compreende: ‘É possível que um Iluminado, Perfeitamente Iluminado, possa
surgir em um mundo - existe essa possibilidade.’ Ele compreende: ‘É impossível,
não pode acontecer que dois Monarcas que façam girar a roda, possam surgir ao
mesmo tempo em um mundo ... É possível que um Monarca que faça girar a roda,
possa surgir em um mundo - existe essa possibilidade.’</p>



<p class=Normal>15. “Ele compreende: ‘É impossível, não pode acontecer que
uma mulher possa ser um Iluminado, Perfeitamente Iluminado - não existe essa
possibilidade.’[<a href="#N13">13</a>] <a name=R13></a>E ele compreende: ‘É
possível que um homem possa ser um Iluminado, Perfeitamente Iluminado - existe
essa possibilidade.’ Ele compreende: ‘É impossível, não pode acontecer que uma
mulher possa ser um Monarca que faça girar a roda ... que uma mulher possa
ocupar a posição de Sakka ... que uma mulher possa ocupar a posição de<span
style="mso-spacerun: yes">  </span>Mara ... que uma mulher possa ocupar a
posição de Brahma - não existe essa possibilidade.’ E ele compreende: ‘É
possível que um homem possa ser um Monarca que faça girar a roda ... que um
homem possa ocupar a posição de Sakka ... que um homem possa ocupar a posição
de<span style="mso-spacerun: yes">  </span>Mara ... que um homem possa ocupar a
posição de Brahma<span style="mso-spacerun: yes">  </span>- existe essa
possibilidade.’ </p>



<p class=Normal>16. “Ele compreende: ‘É impossível, não pode acontecer que
um resultado não desejado, desagradável, possa ser produzido de uma boa conduta
com o corpo … boa conduta com a linguagem … boa conduta com a mente - não
existe essa possibilidade.’ E ele compreende: ‘É possível que um resultado
desejado, agradável, possa ser produzido de uma boa conduta com o corpo … boa
conduta com a linguagem … boa conduta com a mente<span style="mso-spacerun:
yes">  </span>- existe essa possibilidade.’ </p>



<p class=Normal>17. “Ele compreende: ‘É impossível, não pode acontecer que
uma pessoa que se ocupe com o comportamento impróprio com o corpo … que se ocupe
com o comportamento impróprio com a linguagem ... que se ocupe com o
comportamento impróprio com a mente possa por conta disso, devido a isso, na
dissolução do corpo, após a morte, renascer num destino feliz, até mesmo no
paraíso – não existe essa possibilidade.’ [<a href="#N14">14</a>] <a name=R14></a>E
ele compreende: ‘É possível que uma pessoa que se ocupe com o comportamento
impróprio com o corpo … que se ocupe com o comportamento impróprio com a linguagem
... que se ocupe com o comportamento impróprio com a mente possa por conta
disso, devido a isso, na dissolução do corpo, após a morte, renascer num estado
de privação, num destino infeliz, nos reinos inferiores, até mesmo no inferno -
existe essa possibilidade.’ </p>


<p class=Normal>18. “Ele compreende: ‘É impossível, não pode acontecer que
uma pessoa que se ocupe com o comportamento apropriado com o corpo … que se
ocupe com o comportamento apropriado com a linguagem ... que se ocupe com o
comportamento apropriado com a mente possa por conta disso, devido a isso, na
dissolução do corpo, após a morte, renascer num estado de privação, num destino
infeliz, nos reinos inferiores, até mesmo no inferno - não existe essa
possibilidade.’ E ele compreende: ‘É possível que uma pessoa que se ocupe com o
comportamento apropriado com o corpo … que se ocupe com o comportamento apropriado
com a linguagem ... que se ocupe com o comportamento apropriado com a mente
possa por conta disso, devido a isso, na dissolução do corpo, após a morte
renascer num destino feliz, até mesmo no paraíso - existe essa possibilidade.’ </p>



<p class=Normal>Dessa forma, Ananda, um bhikkhu pode ser chamado de hábil
com aquilo que é possível e aquilo que é impossível.”</p>



<p class=Normal>19. Quando isso foi dito, o venerável Ananda disse para o Abençoado:
“É maravilhoso, venerável senhor, é admirável! Qual é o nome deste discurso do
Dhamma?”</p>

<p class=Normal>“Você pode se recordar deste discurso do Dhamma, Ananda como
‘Os Muitos Tipos de Elementos’ e ‘Os Quatro Ciclos’ [<a href="#N15">15</a>] <a
name=R16></a><a name=R15></a>e como ‘O Espelho do Dhamma’ e o ‘Tambor do
Imortal’ e como a ‘Vitória Suprema em Batalha.’”</p>



