<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] Os dezoito elementos estão definidos no Vbh </span>183-84/
87-90 e são explicados em detalhe no Vsm XV, 17-43. Em suma, o
elemento mente, (<i>manodhatu</i>), de acordo com o Abhidhamma, inclui a
consciência de advertência para os cinco objetos que colidem com as cinco
faculdades dos sentidos, (<i>pancadvaravajjana-citta</i>), e a consciência que
recebe o objeto depois que este tenha sido percebido através dos sentidos, (<i>sampaticchana-citta</i>).
O elemento consciência na mente, (<i>manoviññanadhatu</i>), inclui todos os
tipos de consciências, exceto as cinco consciências nos sentidos e o elemento mente.
O elemento objeto mental, (<i>dhammadhatu</i>), inclui os tipos de fenômenos
materiais sutis que não envolvem a percepção dos sentidos, os três agregados mentais,
da sensação, da percepção e das formações, e Nibbana. Não inclui conceitos,
idéias abstratas, julgamentos, etc. Embora estes últimos estejam incluídos na
noção de objeto mental, (<i>dhammarammana</i>), o elemento objeto mental inclui
apenas as coisas que existem por sua própria natureza, não coisas fabricadas
pela mente. [<a href="#R1">Retorna</a>]<span lang=PT-BR style='mso-ansi-language:
PT-BR'><o:p></o:p></span></p>


<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[2] Eles estão definidos no Vbh </span>180/85-86. Os elementos prazer
e dor são sensações no corpo, prazerosas e dolorosas; os elementos alegria e
tristeza são sensações na mente, prazerosas e dolorosas; o elemento equanimidade
é a sensação nem dolorosa, nem prazerosa. MA diz que a ignorância é incluída
devido à sua aparente semelhança ao elemento da equanimidade.[<a href="#R2">Retorna</a>]<span
lang=PT-BR style='mso-ansi-language:PT-BR'><o:p></o:p></span></p>


<p class=Normal><a name=N3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[3] Vbh </span>183/86-87 define-os como os seis tipos
correspondentes do pensamento aplicado, (<i>vitakka</i>); veja o <a
href="MN19.php#T2">MN 19.2</a>.[<a href="#R3">Retorna</a>]<span lang=PT-BR
style='mso-ansi-language:PT-BR'><o:p></o:p></span></p>


<p class=Normal><a name=N4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[4] </span>MA explica o elemento sensual como os cinco agregados que
pertencem à esfera sensual, (<i>kamavacara</i>), o elemento matéria sutil como
os cinco agregados que pertencem à esfera da matéria sutil, (<i>rupavacara</i>),
e o elemento imaterial como os quatro agregados que pertencem à esfera
imaterial, <i>(arupavacara</i>).[<a href="#R4">Retorna</a>]<span lang=PT-BR
style='font-size:11.0pt;mso-bidi-font-size:12.0pt;mso-ansi-language:PT-BR'><o:p></o:p></span></p>


<p class=Normal><a name=N5></a> [5] </span>MA: o elemento
condicionado inclui tudo aquilo que é produzido por condições e é uma
designação para os cinco agregados. O elemento não condicionado é Nibbana. [<a
href="#R5">Retorna</a>]<span lang=PT-BR style='mso-ansi-language:PT-BR'><o:p></o:p></span></p>


<p class=Normal><a name=N6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[6] As doze bases são definidas no</span> Vbh 155-167/70-73 e
explicadas no Vsm XV, 1-16. A base da mente inclui todos os tipos de
consciência e assim conhece todos os sete elementos que exercem a função da
consciência. A base do objeto mental é idêntica ao elemento objeto mental.[<a
href="#R6">Retorna</a>]<span lang=PT-BR style='mso-ansi-language:PT-BR'><o:p></o:p></span></p>

<p class=Normal><a name=N7></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[7] </span>MA: Uma pessoa que possui o entendimento correto, (<i>ditthisampanno</i>),
é aquela que possui o entendimento do caminho, um nobre discípulo com o nível mínimo
do ‘entrar na correnteza.’ “Formação”, neste caso, deve ser compreendida como
formação condicionada, (<i>sankhatasankhara</i>), isto é, qualquer coisa
condicionada. [<a href="#R7">Retorna</a>]<span lang=PT-BR style='mso-ansi-language:
PT-BR'><o:p></o:p></span></p>


<p class=Normal><a name=N8></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[8] </span>MA aponta que um nobre discípulo num nível abaixo de um
arahant ainda pode perceber as formações como prazerosas com a mente dissociada
do entendimento incorreto, mas ele não será capaz de adotar o entendimento de
que qualquer formação é prazerosa. Embora as percepções e pensamentos sobre as
formações como prazerosas surjam nele, ele sabe através da reflexão que essas
noções são equivocadas. [<a href="#R8">Retorna</a>]<span lang=PT-BR
style='mso-ansi-language:PT-BR'><o:p></o:p></span></p>


<p class=Normal><a name=N9></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[9] No trecho sobre o eu</span>, <i>sankhara</i>, “formação,” é
substituída por <i>dhamma</i>, “fenômeno.” MA explica que essa substituição é feita
para incluir conceitos, tal como o sinal de uma kasina, etc., que uma pessoa
comum também tende a identificar como eu. No entanto, visto que Nibbana é
descrito como imperecível, (<i>accuta</i>), e como felicidade, (<i>sukha</i>),
e como também está sujeito a ser confundido com o eu, (veja o <a
href="MN1.php#T26">MN 1.26</a>), a palavra <i>sankhara</i> pode ser tomada de
modo a incluir apenas o condicionado, enquanto que <i>dhamma</i> inclui ambos,
o condicionado e o incondicionado. Esta interpretação, no entanto, não é endossada
pelos comentários de Acariya Buddhaghosa. [<a href="#R9">Retorna</a>]<span
