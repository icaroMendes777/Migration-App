<p class=Normal><a name=N5></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[5] O texto parece ser redundante ao mencionar
ambas, as formas, (<i>rupa</i>), e as coisas que podem ser conscientizadas pela no olho, (<i>cakkhuviññana-viññatabba dhamma</i>).
MA menciona duas opiniões sugeridas para solucionar este problema. Uma
argumenta que “formas” se refere a coisas visíveis que de fato são percebidas,
“coisas que podem ser percebidas...” aqueles objetos visíveis que cessam sem
serem percebidos. A segunda diz que o primeiro termo significa todas as formas
sem distinção, o último termo os três agregados mentais que funcionam em
associação com a consciência no olho. [<a href="#R5">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[6] MA explica a “fabricação de um eu”, (<i>ahankara</i>),
como presunção e “fabricação do meu”, (<i>mamankara</i>), como desejo. “Todos
os sinais externos”, (<i>nimitta</i>), são os objetos externos. [<a href="#R6">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N7></a><span lang=PT-BR style='mso-ansi-language:
