<p class=Normal> Assim ouvi. Em certa
ocasião, o Abençoado estava em Benares, no Parque do Gamo, em Isipatana Lá ele se dirigiu ao grupo de cinco bhikkhus:
<o:p></o:p></span></p>


<p class=Normal> “Bhikkhus, há esses dois extremos aos quais aquele que abandonou a
vida em família e seguiu a vida santa não deve se entregar. Quais dois? A busca
da felicidade nos prazeres sensuais, que são baixos, vulgares, grosseiros, ignóbeis
e que não trazem benefício; e a busca da mortificação, que é dolorosa, ignóbil
e que não traz benefício. Evitando esses dois extremos o Tathagata despertou
para o Caminho do Meio, que faz surgir a visão, que faz surgir a sabedoria, que
conduz à paz, ao conhecimento direto, à iluminação, a Nibbana.<o:p></o:p></span></p>


<p class=Normal> “E qual, bhikkhus, é o caminho do meio para o qual o Tathagata
despertou, que faz surgir a visão ... que conduz a Nibbana? É este Nobre
Caminho Óctuplo: entendimento correto, pensamento correto, linguagem correta,
ação correta, modo de vida correto, esforço correto, atenção plena correta,
concentração correta. Esse, bhikkhus, é o caminho do meio para o qual o
Tathagata despertou, que faz surgir a visão, que faz surgir a sabedoria, que
conduz à paz, ao conhecimento direto, à iluminação, a Nibbana.<o:p></o:p></span></p>


<p class=Normal> “Agora, bhikkhus, esta é a nobre verdade do sofrimento: nascimento é
sofrimento, envelhecimento é sofrimento, enfermidade é sofrimento, morte é
sofrimento; tristeza, lamentação, dor, angústia e desespero são sofrimento; a
união com aquilo que é desprazeroso é sofrimento; a separação daquilo que é
prazeroso é sofrimento; não obter o que se deseja é sofrimento; em resumo, os
cinco agregados influenciados pelo apego são sofrimento.<o:p></o:p></span></p>


<p class=Normal> “Agora, bhikkhus, esta é a nobre verdade da origem do sofrimento: é
este desejo que conduz a uma renovada existência,<span style="mso-spacerun:
yes">  </span>acompanhado pela cobiça e pelo prazer, buscando o prazer aqui e
ali; isto é, o desejo pelos prazeres sensuais, o desejo por ser/existir, o
desejo por não ser/existir.<o:p></o:p></span></p>


<p class=Normal><a name="3nv"> </a> “Agora, bhikkhus, esta é a nobre verdade da cessação do sofrimento: é o
desaparecimento e cessação sem deixar vestígios daquele mesmo desejo, abrir mão, descartar, 
libertar-se, despegar desse mesmo desejo.[<a href="#N1">1</a>]<o:p></o:p></span></p>


<p class=Normal><a name="R1"> </a> “Agora, bhikkhus, esta é a nobre verdade do caminho que conduz à
cessação do sofrimento: é este Nobre Caminho Óctuplo: entendimento correto,
pensamento correto, linguagem correta, ação correta, modo de vida correto,
esforço correto, atenção plena correta, concentração correta. <o:p></o:p></span></p>


<p class=Normal> “’Esta é a nobre verdade do sofrimento’: assim, bhikkhus, com relação a
coisas não ouvidas antes, surgiram em mim a visão,<span style="mso-spacerun:
yes">  </span>a compreensão, a sabedoria, o verdadeiro conhecimento e a
iluminação.<span style="mso-spacerun: yes">  </span><o:p></o:p></span></p>


<p class=Normal> “’Esta nobre verdade do sofrimento deve ser completamente compreendida:
assim, bhikkhus, com relação a coisas não ouvidas antes, surgiram em mim a
visão, a compreensão, a sabedoria, o verdadeiro conhecimento e a iluminação.<o:p></o:p></span></p>


<p class=Normal> “’Esta nobre verdade do sofrimento foi completamente compreendida:
assim, bhikkhus, com relação a coisas não ouvidas antes, surgiram em mim a
visão, a compreensão, a sabedoria, o verdadeiro conhecimento e a iluminação.<o:p></o:p></span></p>


<p class=Normal> “’ Esta é a nobre verdade da origem do sofrimento: assim, bhikkhus, com
relação a coisas não ouvidas antes, surgiram em mim a visão, a compreensão, a
sabedoria, o verdadeiro conhecimento e a iluminação.<o:p></o:p></span></p>


<p class=Normal> “’Esta nobre verdade da origem do sofrimento deve ser abandonada:
assim, bhikkhus, com relação a coisas não ouvidas antes, surgiram em mim a
visão, a compreensão, a sabedoria, o verdadeiro conhecimento e a iluminação.<o:p></o:p></span></p>

<p class=Normal> “’Esta nobre verdade da origem do sofrimento foi abandonada: assim,
bhikkhus, com relação a coisas não ouvidas antes, surgiram em mim a visão, a
compreensão, a sabedoria, o verdadeiro conhecimento e a iluminação.<o:p></o:p></span></p>


<p class=Normal> “’Esta é a nobre verdade da cessação do sofrimento: assim, bhikkhus,
com relação a coisas não ouvidas antes, surgiram em mim a visão, a compreensão,
a sabedoria, o verdadeiro conhecimento e a iluminação.<o:p></o:p></span></p>


<p class=Normal> “’Esta nobre verdade da cessação do sofrimento deve ser realizada:
assim, bhikkhus, com relação a coisas não ouvidas antes, surgiram em mim a
visão,<span style="mso-spacerun: yes">  </span>a compreensão,<span
style="mso-spacerun: yes">  </span>a sabedoria, o verdadeiro conhecimento e a
iluminação.<o:p></o:p></span></p>


<p class=Normal> “’Esta nobre verdade da cessação do sofrimento foi realizada: assim, bhikkhus,
com relação a coisas não ouvidas antes, surgiram em mim a visão, a compreensão,
a sabedoria, o verdadeiro conhecimento e a iluminação.<o:p></o:p></span></p>


<p class=Normal> “’Esta é a nobre verdade do caminho que conduz à cessação do
sofrimento: assim, bhikkhus, com relação a coisas não ouvidas antes, surgiram
em mim a visão, a compreensão, a sabedoria, o verdadeiro conhecimento e a
iluminação.<o:p></o:p></span></p>


<p class=Normal> “’Esta nobre verdade do caminho que conduz à cessação do sofrimento
deve ser desenvolvida: assim, bhikkhus, com relação a coisas não ouvidas antes,
surgiram em mim a visão, a compreensão, a sabedoria, o verdadeiro conhecimento
e a iluminação.<o:p></o:p></span></p>


<p class=Normal> “’Esta nobre verdade do caminho que conduz à cessação do sofrimento foi
desenvolvida: assim, bhikkhus, com relação a coisas não ouvidas antes, surgiram
em mim a visão, a compreensão,<span style="mso-spacerun: yes">  </span>a
sabedoria, o verdadeiro conhecimento e a iluminação.<o:p></o:p></span></p>


<p class=Normal> “Enquanto, bhikkhus, meu conhecimento e visão dessas Quatro Nobres
Verdades como na verdade elas são, nas suas três fases e doze aspectos, [<a
href="#N2">2</a>] <a name=R2></a>não estava completamente purificado desse
modo, não reivindiquei ter despertado para a insuperável perfeita iluminação
neste mundo com os seus devas, maras e brahmas, esta população com seus
contemplativos e brâmanes, seus príncipes e povo. Mas quando meu conhecimento e
visão dessas Quatro Nobres Verdades como na verdade elas são, nas suas três
fases e doze aspectos, estava completamente purificado desse modo, reivindiquei
ter despertado para a insuperável perfeita iluminação neste mundo com os seus devas,
maras e brahmas, esta população com seus contemplativos e brâmanes, seus
príncipes e povo. O conhecimento e visão surgiram em mim: ‘A libertação da
minha mente é inabalável. Este é o meu último nascimento. Não há mais vir a
ser a nenhum estado.’<o:p></o:p></span></p>


<p class=Normal> Isto foi o que o Abençoado disse. Os cinco bhikkhus ficaram satisfeitos e contentes com
as palavras do Abençoado. E enquanto o Abençoado discursava, o olho
imaculado do Dhamma surgiu no Venerável Kondañña: ‘Tudo que está sujeito ao surgimento está sujeito à cessação.’<o:p></o:p></span></p>


<p class=Normal> E quando a roda do Dhamma havia sido colocada em movimento pelo
Abençoado, os devas da terra exclamaram: “Em  Benares, no Parque do Gamo, em Isipatana, esta insuperável
roda do Dhamma foi colocada em movimento pelo Abençoado, e não poderá ser
detida por nenhum brâmane ou contemplativo, ou deva, ou Mara, ou Brahma, ou
qualquer um no mundo.” Ouvindo a exclamação dos devas da terra, os devas do
Reino dos Quatro Grandes Reis também exclamaram… os devas dos Trinta e Três….
os devas do Yama…. os devas do Tusita… os devas do Nimmanarati… os devas do
Paranimmita-vasavatti…. os devas do cortejo de Brahma também exclamaram:<span
style="mso-spacerun: yes">  </span>“Em  Benares, no Parque do Gamo, em Isipatana, esta insuperável
roda do Dhamma foi colocada em movimento pelo Abençoado, e não poderá ser
detida por nenhum brâmane ou contemplativo, ou deva, ou Mara, ou Brahma, ou
qualquer um no mundo.”<o:p></o:p></span></p>


<p class=Normal> Assim, naquele momento, naquele instante, naquele segundo, a exclamação
subiu até os distantes mundos de Brahma e esse sistema cósmico com dez mil
mundos tremeu, estremeceu e se abalou e uma intensa, imensurável e gloriosa
luminosidade surgiu no mundo ultrapassando a divina majestade dos devas.<o:p></o:p></span></p>


<p class=Normal> Então, o Abençoado exclamou: “Kondañña de fato compreendeu! Kondañña de
fato compreendeu!” E foi assim que o Venerável Kondañña adquiriu o nome Añña-Kondañña – Kondañña aquele que Compreendeu.”<o:p></o:p></span></p>



