<p class=Normal><b> Notas:<o:p></o:p></span></b></p>


<p class=Normal> <a name=N1></a><span lang=PT-BR>[1] Em Pali: <i>caga, patinissagga, mutti, analaya</i>. 
<i>Caga</i>, abrir mão, também ocorre com freqüência como significando generosidade. 
Ajaan Brahm interpreta que já neste primeiro discurso o Buda traça as linhas 
mestras do seu método de desenvolvimento mental que está baseado no abandono, na renúncia, no soltar-se.
A interpretação de Ajaan Brahm para cada um dos termos em Pali é a seguinte:<br>
<i>caga</i>: generosidade – entregar-se com o corpo e a mente, de forma completa para a 
prática meditativa sem esperar absolutamente nada em retorno. Essa é a perfeição da 
generosidade, dar tudo sem esperar receber nada em troca.<br>
<i>patinissagga</i>: descartar – na meditação não se deve esperar obter alguma coisa mas 
sim abandonar as coisas, livrar-se das possessões mentais: os pensamentos, 
memórias, experiências, idéias, medos, de modo que o meditador esteja livre e absolutamente 
vazio. O propósito da meditação não é acumular coisas mas livrar-se delas.<br>
<i>mutti</i>: liberdade – qualquer lugar onde não queremos estar se transforma numa prisão, 
enquanto que em qualquer lugar onde queremos estar, onde estejamos felizes, nos sentimos livres. Sentindo-se 
feliz na meditação não há modo que o desejo possa surgir e interferir na meditação, é uma questão de atitude.<br>
<i>analaya</i>: despegar – nada gruda ou se estabelece na mente, nem mesmo as experiências belas, 
as coisas não são acumuladas, há sempre o vazio na mente, as coisas ocorrem e são abandonadas 
pois imediatamente passam a pertencer ao passado.<br>
Estas notas foram compostas de uma palestra do Dhamma de Ajaan Brahm
no retiro da Páscoa de 2007. </a>
[<a href="#R1">Retorna</a>]</span></p>




<p class=Normal> <a name=N2></a><span lang=PT-BR>[2] As três fases são: (i)
o conhecimento de cada verdade, ex: “Esta é a nobre verdade do sofrimento”;
(ii) o conhecimento da tarefa a ser realizada com relação a cada verdade, ex:
“Esta nobre verdade do sofrimento deve ser completamente compreendida”; (iii) o
conhecimento de ter completado a tarefa em relação a cada verdade, ex: “Esta
nobre verdade do sofrimento foi completamente compreendida.” Os doze aspectos
são obtidos aplicando as três fases às quatro verdades. Este tipo de esquema,
na tradição legal e filosófica hindu, é chamado de roda. Neste caso a roda tem
doze raios, unidas no cubo pelo conhecimento e visão das coisas como elas na
verdade são. No entanto, os textos antigos que tratam da roda do Dhamma contêm
muito mais do que os doze raios, sendo a forma encontrada para indicar que a
roda está em movimento. [<a href="#R2">Retorna</a>]</span></p>


