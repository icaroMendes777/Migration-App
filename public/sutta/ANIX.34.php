<p class=Normal>Assim ouvi. Em certa ocasião o Ven. Sariputta estava em
Rajagaha, no Bambual, no Santuário dos Esquilos. Lá ele disse para os bhikkhus,
“Este Nibbana é prazeroso, amigos. Este Nibbana é prazeroso, amigos.” </p>

<p class=Normal>Quando isso foi dito, o Ven. Udayin disse para o <a name=PVW>Ven</a>.
Sariputta, “Mas que prazer pode haver aqui, meu amigo, onde nada é sentido?” </p>

<p class=Normal>“Exatamente esse é o prazer aqui, meu amigo: onde nada é sentido. Existem
esses cinco elementos do prazer sensual. Quais cinco? Formas conscientizadas através
do olho que são desejáveis, agradáveis e fáceis de serem gostadas, conectadas
com o desejo sensual e que provocam a cobiça. Sons conscientizados através do ouvido
… Aromas conscientizados através do nariz … Sabores conscientizados através da língua
…Tangíveis conscientizados através do corpo que são desejáveis, agradáveis e fáceis
de serem gostados, conectados com o desejo sensual e que provocam a cobiça.
Qualquer prazer ou alegria que surja na dependência desses cinco elementos do
prazer sensual, isso é o prazer sensual. </p>

<p class=Normal>“Agora há o caso em que um bhikkhu afastado dos prazeres sensuais, afastado das qualidades não hábeis, entra e permanece no primeiro jhana, que é caracterizado pelo pensamento aplicado e sustentado, com o êxtase e felicidade nascidos do afastamento. Se,
enquanto assim permanecer, ele for atormentado pela atenção para as percepções
que digam respeito aos <span lang=PT-BR style='mso-ansi-language:PT-BR'>prazeres
dos sentidos</span>, isso será para ele uma aflição. Tal como, devido a uma
enfermidade, a dor surge como uma aflição numa pessoa saudável, da mesma forma
a atenção para as percepções que digam respeito aos <span lang=PT-BR
style='mso-ansi-language:PT-BR'>prazeres dos sentidos</span> que atormentam um
bhikkhu é para ele uma aflição. Agora, o Abençoado disse que qualquer aflição é
sofrimento. Portanto, através desse raciocínio pode ser compreendido que
Nibbana é prazeroso. </p>

<p class=Normal>“Além disso, abandonando o pensamento aplicado e sustentado, um bhikkhu entra e permanece no segundo jhana, que é caracterizado pela segurança interna e perfeita unicidade da mente, sem o pensamento aplicado e sustentado, com o êxtase e felicidade nascidos da concentração.  Se, enquanto assim permanecer, ele for atormentado pela
atenção para as percepções que digam respeito ao pensamento aplicado e sustentado,
isso será para ele uma aflição... </p>

<p class=Normal>“Além disso, abandonando o êxtase, um bhikkhu entra e permanece no terceiro jhana que é caracterizado pela felicidade sem o êxtase, acompanhada pela atenção plena, plena consciência e equanimidade, acerca do qual os nobres declaram: ‘Ele permanece numa estada feliz, equânime e plenamente atento.’ Se, enquanto
assim permanecer, ele for atormentado pela atenção para as percepções que digam
respeito ao êxtase, isso será para ele uma aflição...</p>

<p class=Normal>“Além disso, com o completo desaparecimento da felicidade, um bhikkhu entra e permanece no quarto jhana, que possui nem felicidade nem sofrimento, com a atenção plena e a equanimidade purificadas. Se, enquanto assim permanecer, ele for atormentado pela
atenção para as percepções que digam respeito à <span lang=PT-BR
style='mso-ansi-language:PT-BR'>felicidade e ao sofrimento</span>, isso será
para ele uma aflição...</p>

<p class=Normal>“Além disso, há o caso em que um bhikkhu, <span lang=PT-BR style='mso-ansi-language:
PT-BR'>com a completa superação das percepções da forma, com o desaparecimento
das percepções do contato sensorial, sem dar atenção às percepções da
diversidade, consciente de que o ‘espaço é infinito,’ entra e permanece na base
do espaço infinito</span>. Se, enquanto assim permanecer, ele for atormentado
pela atenção para as percepções que digam respeito à <span lang=PT-BR
style='mso-ansi-language:PT-BR'>forma</span>, isso será para ele uma aflição...</p>

<p class=Normal>“Além disso, há o caso em que um bhikkhu, <span lang=PT-BR style='mso-ansi-language:
PT-BR'>com a completa superação da base do espaço infinito, consciente de que a
‘consciência é infinita,’ entra e permanece na base da consciência infinita, e
ele se torna consciente dessa verdadeira porém sutil percepção da esfera da
consciência infinita</span>. Se, enquanto assim permanecer, ele for atormentado
pela atenção para as percepções que digam respeito à <span lang=PT-BR
style='mso-ansi-language:PT-BR'>base do espaço infinito</span>, isso será para
ele uma aflição...</p>

<p class=Normal>“Além disso, há o caso em que um bhikkhu, <span lang=PT-BR style='mso-ansi-language:
PT-BR'>com a completa superação da base da consciência infinita, consciente de
que ‘não há nada,’ entra e permanece na base do nada</span>. Se, enquanto assim
permanecer, ele for atormentado pela atenção para as percepções que digam
respeito à <span lang=PT-BR style='mso-ansi-language:PT-BR'>base da consciência
infinita</span>, isso será para ele uma aflição...</p>

<p class=Normal>“Além disso, há o caso em que um bhikkhu, <span lang=PT-BR style='mso-ansi-language:
PT-BR'>com a completa superação da base do nada, entra e permanece na base da
nem percepção, nem não percepção.</span> Se, enquanto assim permanecer, ele for
atormentado pela atenção para as percepções que digam respeito à <span
lang=PT-BR style='mso-ansi-language:PT-BR'>base do nada</span>, isso será para
ele uma aflição. Agora, o Abençoado disse que qualquer aflição é sofrimento.
Portanto, através desse raciocínio pode ser compreendido que Nibbana é
prazeroso. </p>

<p class=Normal>“Além disso, há o caso em que um bhikkhu, <span lang=PT-BR style='mso-ansi-language:
PT-BR'>com a completa superação da base da nem percepção, nem não percepção,
entra e permanece na cessação da percepção e sensação. E vendo isso com
sabedoria, as suas impurezas mentais são completamente destruídas. </span>Portanto,
através desse raciocínio pode ser compreendido que Nibbana é prazeroso.”</p>

