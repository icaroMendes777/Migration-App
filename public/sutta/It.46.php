<p class=Normal> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: “Bhikkhus, vivam para realizar os benefícios deste treinamento, a sabedoria superior, o cerne da libertação, e a atenção plena como princípio governante. Bhikkhus, se vocês viverem para realizar os benefícios do treinamento ... um de dois frutos podem ser esperados: completa compreensão aqui e agora, ou se houver alguma contaminação residual, o estado de não-retorno.”</p>


<p class=Normal style='margin-left:.75in'> Aquele que concluiu o treinamento, <br>
incapaz de decair, <br>
tendo realizado a sabedoria superior, <br>
viu o fim do nascimento –<br>
Esse sábio suporta o último corpo, <br>
e tendo deixado de lado a presunção, <br>
ele superou o envelhecimento, eu digo.</p>

<p class=Normal style='margin-left:.75in'> Portanto, sempre se deliciando nos jhanas, <br>
concentrado, ardente, <br>
vendo o fim do nascimento, Oh bhikkhus, <br>
conquistem Mara e a sua horda, <br>
superem o nascimento e a morte.</p>




