elas são represadas, diga-me como contê-las, e como por fim elas são bloqueadas?”
<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><i>O Buda:</i>
“Quaisquer torrentes que existam no mundo, é a atenção plena que as represa e
contem, e é através da sabedoria que elas por fim são bloqueadas.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><i>Ajita:</i> “É
apenas sabedoria e atenção plena. Agora a mentalidade-materialidade (nome e forma), venerável
senhor, explique isto: onde ela cessa?” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><i>O Buda:</i> “Esta
é a resposta à sua questão, <i>Ajita:</i> com a cessação da consciência a
mentalidade-materialidade (nome e forma) cessa.” [<a href="#N2">2</a>] <o:p></o:p></span></p>



<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'><i>Ajita:</i> “Aqueles que compreenderam completamente o Dhamma, aqueles em
treinamento e os outros indivíduos aqui, [<a href="#N3">3</a>] <a name=R3></a>explique-me
como eles se comportam.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><i>O Buda:</i>
