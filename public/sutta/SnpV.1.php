<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] “As torrentes” são os desejos que fluem na direção de objetos
desejosos e prazerosos no mundo. [<a href="#R1">Retorna</a>] <o:p></o:p></span></p>



<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[2] Esta pergunta e a resposta se referem à doutrina da origem
dependente (<i>paticca-samuppada</i>). Veja também o <a
href="samyutta_nikaya.php#RXII">Nidana-samyutta</a>. [<a href="#R2">Retorna</a>]
<o:p></o:p></span></p>



<p class=Normal><a name=N3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[3] “Aqueles que compreenderam completamente” são os arahants. “Aqueles
em treinamento” são os <i>sekhas</i> que estão empenhados pelo objetivo, mas
têm a segurança de que o objetivo será alcançado. Os “outros indivíduos” são os
seres comuns (<i>puthujjana</i>) que não têm nenhuma realização e não
conquistaram a segurança de que o objetivo será alcançado. [<a href="#R3">Retorna</a>]
<o:p></o:p></span></p>



<p class=Normal><a name=N4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[4] A palavra <i>anavilo</i> significa puro, límpido, tranqüilo, sem
agitação, sem confusão, etc. No <a
href="../dhp/dhp6.php">Dhammapada v. 82</a>, os sábios são
comparados com um lago profundo com essa qualidade. [<a href="#R4">Retorna</a>]<o:p></o:p></span></p>









