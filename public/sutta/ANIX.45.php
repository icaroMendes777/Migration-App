<p class=Normal> [Udayin:] “’Libertado de ambos os modos, libertado de ambos os modos,’ dizem. De que modo alguém é descrito pelo Abençoado como libertado de ambos os modos?” </p>

<p class=Normal> [Ananda:] “É o caso, amigo, em que um bhikkhu, afastado dos prazeres sensuais, afastado das qualidades não hábeis, entra e permanece no primeiro jhana, que é caracterizado pelo pensamento aplicado e sustentado, com o êxtase e felicidade nascidos do afastamento. Ele permanece tocando com o seu corpo sempre que houver uma oportunidade, e ele compreende isso através da sabedoria. É desse modo que alguém é descrito pelo Abençoado de um modo seqüencial como libertado de ambos os modos. </p>

<p class=Normal> “Além disso,  abandonando o pensamento aplicado e sustentado, ele entra e permanece no segundo jhana … no terceiro jhana ... no quarto jhana ... na base do espaço infinito ... na base da consciência infinita ... na base do nada ... na base da nem percepção nem não percepção. Ele permanece tocando como o seu corpo sempre que houver uma oportunidade, e ele compreende isso através da sabedoria. É desse modo que alguém é descrito pelo Abençoado de um modo seqüencial como libertado de ambos os modos. </p>

<p class=Normal> “Além disso, com a completa superação da base da nem percepção nem não percepção, ele entra e permanece na cessação da percepção e sensação. E ao ver com sabedoria, as suas impurezas são eliminadas. Ele permanece tocando como o seu corpo sempre que houver uma oportunidade, e ele compreende isso através da sabedoria. É desse modo que alguém é descrito pelo Abençoado de um modo seqüencial como libertado de ambos os modos.” </p>

