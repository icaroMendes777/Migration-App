<p class=Normal> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: "Bhikkhus, um bhikkhu deve examinar as coisas de uma tal forma que enquanto as examina, a sua consciência não fique distraída e dispersa externamente, nem estabelecida internamente, e por não se apegar a nada ele não fique agitado. Se a consciência dele não estiver distraída e dispersa externamente, nem estabelecida internamente, e por não se apegar a nada ele não se tornar agitado, então para ele não existirá a origem do sofrimento – o nascimento, o envelhecimento e morte no futuro.” [<a href="#N1">1</a>] </p>

<p class=Normal style='margin-left:.75in'> Quando um bhikkhu abandonou<br>
as sete amarras e cortou o cordão [<a href="#N2">2</a>] <br>
a sua perambulação pelos nascimentos está terminada, <br>
não mais haverá a renovação do ser/existir.</p>



