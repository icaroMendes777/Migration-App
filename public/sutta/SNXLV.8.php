<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, eu ensinarei e analisarei para vocês o Nobre Caminho
Óctuplo. </span>Ouçam e prestem muita atenção àquilo que eu vou dizer.” – “Sim,
venerável senhor,” os bhikkhus responderam. O Abençoado disse o seguinte:<span
style='mso-ansi-language:PT-BR'> <span lang=PT-BR><o:p></o:p></span></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual, bhikkhus, é o
Nobre Caminho Óctuplo? Entendimento Correto, Pensamento Correto, Linguagem
Correta, Ação Correta, Modo de Vida Correta, Esforço Correto, Atenção Plena
Correta, Concentração Correta. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;E o que é o
entendimento correto? Compreensão do sofrimento, compreensão da origem do
sofrimento, compreensão da cessação do sofrimento, compreensão do caminho da
prática que conduz à cessação do sofrimento. A isto se chama entendimento
correto.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>E o que é pensamento
correto? O pensamento de renúncia, o pensamento de não má vontade, o pensamento
de não crueldade. A isto se chama pensamento correto.&quot;<span
style="mso-spacerun: yes">  </span><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;E o que é a linguagem
correta? Abster-se da linguagem mentirosa, da linguagem maliciosa, da linguagem
grosseira e da linguagem frívola. A isto se chama linguagem correta.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;E o que é ação
correta? Abster-se de tirar a vida de outros seres, abster-se de tomar aquilo que não for
dado, abster-se da conduta sexual imprópria. A isto se chama de ação
correta.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;E o que é modo de
vida correto? Aqui um nobre discípulo, tendo abandonado o modo de vida
incorreto, obtém o seu sustento através do modo de vida correto. A isto se
chama modo de vida correto.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;E
o que é esforço correto? <b>(i)</b> Aqui,
bhikkhus, um bhikkhu gera desejo para que não surjam estados ruins e
prejudiciais que ainda não surgiram e ele se aplica, estimula a sua energia,
empenha a sua mente e se esforça. <b>(ii) </b>Ele gera desejo em abandonar
estados ruins e prejudiciais que já surgiram e ele se aplica, estimula a sua
energia, empenha a sua mente e se esforça. <b>(iii)</b> Ele gera desejo para
que surjam estados benéficos que ainda não surgiram e ele se aplica, estimula a
sua energia, empenha a sua mente e se esforça. <b>(iv) </b>Ele gera desejo para
a continuidade, o não desaparecimento, o fortalecimento, o incremento e a
realização através do desenvolvimento de estados benéficos que já surgiram e
ele se aplica, estimula a sua energia, empenha a sua mente e se esforça. A isto
se denomina esforço correto.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;E o que é atenção
plena correta? <b>(i)</b> Aqui, bhikkhus,
um bhikkhu permanece contemplando o corpo como um corpo - ardente, plenamente
consciente e com atenção plena, tendo colocado de lado a cobiça e o desprazer
pelo mundo. <b>(ii)</b> Ele permanece
contemplando as sensações como sensações – ardente, plenamente consciente e com
atenção plena, tendo colocado de lado a cobiça e o desprazer pelo mundo. <b>(iii)</b> Ele permanece contemplando a mente como
mente - ardente, plenamente consciente e com atenção plena, tendo colocado de
lado a cobiça e o desprazer pelo mundo. <b>(iv)</b>
Ele permanece contemplando os objetos mentais como objetos mentais - ardente,
plenamente consciente e com atenção plena, tendo colocado de lado a cobiça e o
desprazer pelo mundo. A isto se denomina atenção plena correta. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;E o
que é concentração correta? <b>(i)</b> Aqui,
bhikkhus, um bhikkhu afastado dos prazeres sensuais, afastado das qualidades não hábeis, entra e 
permanece no primeiro jhana, que é caracterizado pelo pensamento aplicado e sustentado, com o êxtase e felicidade nascidos 
do afastamento. <b>(ii)</b> Abandonando o pensamento aplicado e sustentado, um bhikkhu entra e permanece no segundo jhana, 
que é caracterizado pela segurança interna e perfeita unicidade da mente, sem o pensamento aplicado e sustentado, 
com o êxtase e felicidade nascidos da concentração. <b>(iii)</b> Abandonando o êxtase, um bhikkhu entra e permanece no 
terceiro jhana que é caracterizado pela felicidade sem o êxtase, acompanhada pela atenção plena, plena consciência e 
equanimidade, acerca do qual os nobres declaram: ‘Ele permanece numa estada feliz, equânime e plenamente atento.’
<b>(iv)</b> Com o completo desaparecimento da felicidade, um bhikkhu entra e permanece no quarto jhana, que possui nem felicidade nem sofrimento, com a atenção plena e a equanimidade purificadas.  A isto se denomina concentração correta.&quot; </span></p>


