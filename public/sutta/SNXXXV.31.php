<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus, eu
ensinarei para vocês o caminho que é adequado para desenraizar todas as
concepções. Ouçam e prestem muita atenção àquilo que eu vou dizer.” – “Sim,
venerável senhor,” os bhikkhus responderam. </span>O Abençoado disse o
seguinte:<span style='mso-ansi-language:PT-BR'> <span lang=PT-BR><o:p></o:p></span></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é o caminho adequado para desenraizar todas as concepções? Aqui,
bhikkhus, um bhikkhu não concebe [a si mesmo como ] o olho, ele não concebe [a
si mesmo] no olho, ele não concebe [a si mesmo separado] do olho, ele não
concebe, ‘O olho é meu.’ <a name=R2></a>Ele não concebe [a si mesmo como ] as
formas ... a consciência no olho ... o contato no olho ... e qualquer sensação
que surja tendo o contato no olho como condição – quer seja prazerosa, dolorosa
ou nem prazerosa, nem dolorosa – ele não concebe [a si mesmo como ] isso, ele não
concebe [a si mesmo] nisso, ele não concebe [a si mesmo separado] disso, ele
não concebe, ‘Isso é meu.’ Pois, bhikkhus, qualquer coisa que alguém conceba,
qualquer coisa na qual alguém conceba, qualquer coisa da qual alguém conceba,
qualquer coisa que alguém conceber como ‘meu’ – é diferente do concebido. [<a
href="#N1">1</a>] <a name=R1></a> O mundo, tornando-se diferente, apegado a esse
ser/existir, busca o deleite apenas nesse ser/existir. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele não
concebe o ouvido ... Ele não concebe a mente ... os objetos mentais ... a
consciência na mente ... o contato na mente ... e qualquer sensação que surja
tendo o contato na mente como condição ... ele não concebe [a si mesmo como ] isso, ele não
concebe [a
si mesmo] nisso, ele não concebe [a si mesmo separado] disso, ele não concebe, ‘Isso é meu.’ Pois,
bhikkhus, qualquer coisa que alguém conceba, qualquer coisa na qual alguém
conceba, qualquer coisa da qual alguém conceba, qualquer coisa que alguém
conceba como ‘meu’ – é diferente do concebido. O mundo, tornando-se diferente,
apegado a esse tornar-se, busca o deleite apenas nesse tornar-se.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Qualquer
que seja, bhikkhus, a extensão dos agregados, dos elementos e das bases
dos sentidos, ele não concebe [a si mesmo como ] isso, ele não concebe [a si mesmo] nisso, ele não concebe [a si mesmo separado] disso,
ele não concebe, ‘Isso é meu.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Visto que,
ele nada concebe desse modo, ele não se apega a nada no mundo. Não se apegando,
ele não fica agitado. Sem estar agitado, ele realiza Nibbana. Ele compreende
que: ‘O nascimento foi destruído, a vida santa foi vivida, o que deveria ser
feito foi feito, não há mais vir a ser a nenhum estado.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Esse,
bhikkhus, é o caminho adequado para desenraizar todas as concepções.”<o:p></o:p></span></p>




