<p class=Normal> Então o brâmane Janussoni foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse: </p>

<p class=Normal> "O Mestre Gotama também afirma ser um daqueles que vive em celibato?" </p>

<p class=Normal> "Brâmane se alguém pudesse corretamente dizer de alguma pessoa: 'Ele vive em puro e completo celibato - intacto, impecável, imaculado,' é precisamente de mim que alguém poderia dizer isso. Pois eu vivo em puro e completo celibato - intacto, impecável, imaculado." </p>

<p class=Normal> "Mas o que, Mestre Gotama, é uma violação, um defeito, uma mácula na vida celibatária?" </p>

<p class=Normal> (1) "Aqui, brâmane, algum contemplativo ou brâmane, afirmando ser celibatário, na verdade não pratica o ato sexual com mulheres. Mas ele permite que elas o massageiem, banhem, esfreguem. Ele se delicia com isso, deseja isso, e encontra satisfação nisso. Isso é uma violação, um defeito, uma mácula na vida celibatária. Ele é chamado alguém que vive em celibato impuro, que está agrilhoado pelo grilhão da sexualidade. Ele não está livre do nascimento, do envelhecimento e da morte; da tristeza, lamentação, dor, angústia e desespero. Ele não está livre, eu lhe digo, do sofrimento. </p>

<p class=Normal> (2) "Outra vez, algum contemplativo ou brâmane, afirmando ser celibatário, na verdade não pratica o ato sexual com mulheres; nem ele permite que elas o massageiem, banhem, esfreguem. Mas ele se entretem com elas, se diverte com elas e conta piadas para elas ... </p>

<p class=Normal> (3) " ... ele não se entretem com elas, se diverte com elas e conta piadas para elas ... mas ele as olha e encara diretamente nos olhos  .... </p>

<p class=Normal> (4) " ... ele não as olha e encara diretamente nos olhos... mas ele ouve as vozes delas por trás de uma parede ou proteção enquanto elas cantam, riem, conversam ou  choram ... </p>

<p class=Normal> (5) " .... ele não ouve as vozes delas por trás de uma parede ou proteção enquanto elas cantam, riem, conversam ou  choram ... mas ele se recorda de conversar, rir e se entreter com elas no passado ... </p>

<p class=Normal> (6) " ... ele não se recorda de conversar, rir e se entreter com elas no passado .... mas ele olha para um chefe de família ou para o filho de um chefe de família desfrutando provido e dotado dos cinco elementos do prazer sensual ... </p>

<p class=Normal> (7) " ... ele não olha para um chefe de família ou para o filho de um chefe de família desfrutando provido e dotado dos cinco elementos do prazer sensual, mas ele vive a vida santa aspirando pelo renascimento num certo grupo de devas, pensando: 'Com base neste comportamento virtuoso, observância, austeridade, ou vida santa eu me tornarei um deva.' Ele se delicia com isso, deseja isso, e encontra satisfação nisso. Isso é uma violação, um defeito, uma mácula na vida celibatária. Ele é chamado alguém que vive em celibato impuro, que está agrilhoado pelo grilhão da sexualidade. Ele não está livre do nascimento, do envelhecimento e da morte; da tristeza, lamentação, dor, angústia e desespero. Ele não está livre, eu lhe digo, do sofrimento. </p>

<p class=Normal> "Brâmane, enquanto vi não ter abandonado um ou outro desses sete grilhões da sexualidade, não reivindiquei ter despertado para a insuperável perfeita iluminação neste mundo com os seus devas, maras e brahmas, esta população com seus contemplativos e brâmanes, seus príncipes e povo. Mas quando não vi nem mesmo um desses sete grilhões da sexualidade, reivindiquei ter despertado para a insuperável perfeita iluminação neste mundo com os seus devas, maras e brahmas, esta população com seus contemplativos e brâmanes, seus príncipes e povo. O conhecimento e visão surgiram em mim: ‘A libertação da minha mente é inabalável. Este é o meu último nascimento. Não há mais vir a ser.’"</p>

<p class=Normal> Quando isso foi dito, o brâmane Janussoni disse para o Abençoado: "Magnífico, Mestre Gotama! Magnífico, Mestre Gotama! Mestre Gotama esclareceu o Dhamma de várias formas, como se tivesse colocado em pé o que estava de cabeça para baixo, revelasse o que estava escondido, mostrasse o caminho para alguém que estivesse perdido ou segurasse uma lâmpada no escuro para aqueles que possuem visão pudessem ver as formas. Eu busco refúgio no Mestre Gotama, no Dhamma e na Sangha dos bhikkhus. Que o Mestre Gotama me aceite como discípulo leigo que nele buscou refúgio para o resto da minha vida."</p>

