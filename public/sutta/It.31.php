<p class=Normal> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: "Bhikkhus, há duas coisas que não causam remorso. Quais são as duas? Aqui alguém fez o bem, fez aquilo que é benéfico, fez aquilo que é saudável, e não fez o mal, não fez aquilo que é cruel, não fez o que é errado. Ele não sente remorso ao pensar, 'Eu fiz o bem,' e não sente remorso ao pensar, 'Eu não fiz o mal.' Essas, bhikkhus são as duas coisas que não causam remorso." </p>

