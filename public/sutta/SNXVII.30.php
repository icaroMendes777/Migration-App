<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, o ganho, a honraria e fama, eu digo, são um obstáculo
até mesmo para um bhikkhu que é um arahant, aquele com as impurezas
destruídas.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isso
foi dito, o Venerável Ananda perguntou ao Abençoado: “Porque, venerável senhor,
o ganho, a honraria e fama são um obstáculo até mesmo para um bhikkhu com
as impurezas destruídas?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu não
digo, Ananda, que o ganho, a honraria e fama são um obstáculo para a
libertação inabalável da mente dele. Mas eu digo que eles são um obstáculo para
que ele alcance aquelas permanências prazerosas nesta mesma vida que são
alcançadas por aquele que permanece diligente, ardente e decidido. [<a
href="#N1">1</a>] <a name=R1></a>Tão terríveis, Ananda, são o ganho, a honraria
e fama, amargos, vis, obstruem a realização da insuperável segurança contra
o cativeiro. Portanto, Ananda, você deve praticar da seguinte forma: ‘Eu
abandonarei o ganho, a honraria e fama que já surgiram e não permitirei que
o ganho, a honraria e fama que já surgiram persistam obcecando a minha
mente.’ Assim você deveria praticar.”<o:p></o:p></span></p>




