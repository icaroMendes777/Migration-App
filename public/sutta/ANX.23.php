<p class=Normal> "Bhikkhus, há coisas para serem abandonadas através do corpo, não através da linguagem. Há coisas para serem abandonadas através da linguagem, não através do corpo. Há coisas para serem abandonadas nem através do corpo, nem através da linguagem mas repetidamente vendo através da sabedoria. </p>

<p class=Normal> "Quais, bhikkhus, são as coisas para serem abandonadas através do corpo, não através da linguagem? Aqui, um bhikkhu praticou algum ato prejudicial com o corpo. Os seus sábios companheiros na vida santa o investigam e dizem: 'Você cometeu um ato prejudicial com o corpo. Seria realmente muito bom se você abandonasse a conduta corporal imprópria e desenvolvesse a conduta corporal apropriada,' Tendo os seus sábios companheiros bhikkhus investigado e conversado com ele, ele abandona a conduta corporal imprópria e desenvolve a conduta corporal apropriada. Essas são chamadas as coisas para serem abandonadas através do corpo, não através da linguagem. </p>

<p class=Normal> "Quais são as coisas para serem abandonadas através da linguagem, não através do corpo? Aqui, um bhikkhu praticou algum ato prejudicial com a linguagem. Os seus sábios companheiros na vida santa o investigam e dizem: 'Você cometeu um ato prejudicial com a linguagem. Seria realmente muito bom se você abandonasse a conduta verbal imprópria e desenvolvesse a conduta verbal apropriada,' Tendo os seus sábios companheiros bhikkhus investigado e conversado com ele, ele abandona a conduta verbal imprópria e desenvolve a conduta verbal apropriada. Essas são chamadas as coisas para serem abandonadas através da linguagem, não através do corpo. </p>

<p class=Normal> "Quais são as coisas para serem abandonadas nem através do corpo, nem através da linguagem mas repetidamente vendo através da sabedoria? A cobiça não é abandonada nem através do corpo, nem através da linguagem, mas repetidamente vendo através da sabedoria. A raiva ... a delusão ... o ódio ... a hostilidade ... a difamação ... a insolência ... a avareza não é abandonada nem através do corpo, nem através da linguagem, mas repetidamente vendo através da sabedoria. </p>

<p class=Normal> "A inveja ruim, bhikkhus, não deve ser abandonada nem através do corpo, nem através da linguagem, mas repetidamente vendo através da sabedoria. O que é a inveja ruim? Aqui, um chefe de família, ou o filho de um chefe de família, está prosperando em riqueza ou grãos, em prata ou ouro. Um escravo ou algum dependente poderia pensar: 'Ah, que esse chefe de família, ou esse filho de um chefe de família, não prospere em riqueza ou grãos, em prata ou ouro!' Ou então um contemplativo ou brâmane obtém mantos, comida esmolada, moradia, e medicamentos. Outro contemplativo ou brâmane poderia pensar: 'Ah, que esse venerável não obtenha mantos, comida esmolada, moradia, e medicamentos!' Isso é chamado de inveja ruim. A inveja ruim não deve ser abandonada nem através do corpo, nem através da linguagem, mas repetidamente vendo através da sabedoria. </p>

<p class=Normal> "O desejo ruim, bhikkhus, não deve ser abandonado nem através do corpo, nem através da linguagem, mas repetidamente vendo através da sabedoria. O que é o desejo ruim? Aqui, alguém sem fé deseja: 'Que eles me vejam como alguém dotado de fé.' Alguém imoral deseja: 'Que eles me vejam como alguém dotado de virtude.' Alguém com pouco aprendizado deseja: 'Que eles me vejam como alguém estudado.' Alguém que se delicia com as companhias deseja: 'Que eles me vejam como alguém isolado.' Alguém que é preguiçoso deseja: 'Que eles me vejam como alguém energético.' Alguém com a mente confusa deseja: 'Que eles me vejam como alguém com atenção plena.' Alguém desconcentrado deseja: 'Que eles me vejam como alguém concentrado.' Alguém que não é sábio deseja: 'Que eles me vejam como alguém sábio.' Alguém cujas impurezas não foram destruídas deseja: 'Que eles me vejam como alguém com as impurezas destruídas.' Isso é chamado desejo ruim. O desejo ruim não deve ser abandonado nem através do corpo, nem através da linguagem, mas repetidamente vendo através da sabedoria. </p>

<p class=Normal> "Bhikkhus, se a cobiça sujeita aquele bhikkhu e se mantém; se a raiva ... ... a delusão ... o ódio ... a hostilidade ... a difamação ... a insolência ... a avareza ... inveja ruim ... desejo ruim sujeita aquele bhikkhu e se mantém, ele deve ser visto assim: 'Esse venerável não tem a compreensão de modo que não tenha cobiça; assim a cobiça sujeita aquele bhikkhu e se mantém. Esse venerável não tem a compreensão de modo que não tenha raiva ...  delusão ... ódio ... hostilidade ... difamação ... insolência ... avareza ... inveja ruim ... desejo ruim; assim o desejo ruim sujeita aquele bhikkhu e se mantém.'"</p>


