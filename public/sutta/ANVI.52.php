<p class=Normal> Então o brâmane Janussoni foi até o Abençoado e ao chegar ambos se cumprimentaram. Quando a conversa cortês e amigável havia terminado ele sentou a um lado e disse: </p>

<p class=Normal> (1) "Mestre Gotama, qual a aspiração dos khattiyas? Qual é a sua busca? Qual o seu apoio? Qual o seu objetivo? Qual o seu objetivo final?"

<p class=Normal>"Riqueza, brâmane é a aspiração dos khattiyas; a sua busca é pela sabedoria; o seu apoio é o poder; o seu objetivo é território; o seu objetivo final é a soberania." </p>

<p class=Normal> (2) "Mas, mestre Gotama, qual a aspiração dos brâmanes? Qual é a sua busca? Qual o seu apoio? Qual o seu objetivo? Qual o seu objetivo final?" </p>

<p class=Normal>"Riqueza, brâmane é a aspiração dos brâmanes; a sua busca é pela sabedoria; o seu apoio são os Vedas; o seu objetivo é o sacrifício; o seu objetivo final é o mundo de Brahma." </p>

<p class=Normal> (3) "Mas, mestre Gotama, qual a aspiração dos chefes de família? Qual é a sua busca? Qual o seu apoio? Qual o seu objetivo? Qual o seu objetivo final?" </p>

<p class=Normal>"Riqueza, brâmane é a aspiração dos chefes de família; a sua busca é pela sabedoria; o seu apoio é o seu ofício; o seu objetivo é o trabalho; o seu objetivo final é terminar o trabalho." </p>

<p class=Normal> (4) "Mas, mestre Gotama, qual a aspiração das mulheres? Qual é a sua busca? Qual o seu apoio? Qual o seu objetivo? Qual o seu objetivo final?" </p>

<p class=Normal>"Um homem, brâmane é a aspiração das mulheres; a sua busca é por adornos; o seu apoio são os filhos; o seu objetivo é não ter rivais; o seu objetivo final é ter autoridade." </p>

<p class=Normal> (5) "Mas, mestre Gotama, qual a aspiração dos ladrões? Qual é a sua busca? Qual o seu apoio? Qual o seu objetivo? Qual o seu objetivo final?" </p>

<p class=Normal>"O roubo, brâmane é a aspiração dos ladrões; a sua busca é por uma mata cerrada; o seu apoio é a astúcia; o seu objetivo são os lugares escuros; o seu objetivo final é não serem vistos." </p>

<p class=Normal> (6) "Mas, mestre Gotama, qual a aspiração dos contemplativos? Qual é a sua busca? Qual o seu apoio? Qual o seu objetivo? Qual o seu objetivo final?" </p>

<p class=Normal>"Paciência e moderação, brâmane é a aspiração dos contemplativos; a sua busca é pela sabedoria; o seu apoio é o comportamento virtuoso; o seu objetivo é nada; [<a href="#N1">1</a>] o seu objetivo final é nibbana." </p>

<p class=Normal>"É maravilhoso e admirável, Mestre Gotama! O mestre Gotama sabe a aspiração, a busca, o apoio, o objetivo e o objetivo final de khattiyas, brâmanes, chefes de família, mulheres, ladrões e contemplativos. </p>

<p class=Normal>"Magnífico, Mestre Gotama! Magnífico, Mestre Gotama! Mestre Gotama esclareceu o Dhamma de várias formas, como se tivesse colocado em pé o que estava de cabeça para baixo, revelasse o que estava escondido, mostrasse o caminho para alguém que estivesse perdido ou segurasse uma lâmpada no escuro para aqueles que possuem visão pudessem ver as formas. Eu busco refúgio no Mestre Gotama, no Dhamma e na Sangha dos bhikkhus. Que o Mestre Gotama me aceite como discípulo leigo que buscou refúgio para o resto da vida." </p>


