<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
bhikkhus, qualquer forma, quer seja do passado, futuro ou presente, interna ou
externa; grosseira ou sutil; inferior ou superior, próxima ou distante: toda forma
deve ser vista como na verdade é, com correta sabedoria assim: ‘Isso não é meu,
isso não sou eu, isso não é o meu eu.’ </span><span lang=PT-BR
style='mso-bidi-font-size:10.0pt;mso-ansi-language:PT-BR'><o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Qualquer
sensação... </span><span lang=PT-BR style='mso-bidi-font-size:10.0pt;
