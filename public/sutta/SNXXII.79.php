<p class=Normal><span lang=PT-BR>Em Savathi.
“Bhikkhus, qualquer contemplativo ou brâmane que se recorde das suas muitas vidas
passadas se recorda dos cinco agregados influenciados pelo apego, ou de um
deles. Quais cinco? Ao se recordar, ‘Eu tinha tal forma no passado,’ ele estará
se recordando apenas da forma. Ou ao se recordar, ‘Eu tinha tal sensação no
passado,’ ele estará se recordando apenas da sensação. Ou ao se recordar, ‘Eu
tinha tal percepção no passado,’ ele estará se recordando apenas da percepção.
Ou ao se recordar, ‘Eu tinha tais formações volitivas no passado,’ ele estará se
recordando apenas das formações volitivas. Ou ao se recordar, ‘Eu tinha tal consciência
no passado,’ ele estará se recordando apenas da consciência.</span></p>



<p class=Normal><span lang=PT-BR>“E porque
chamam-na de forma? ‘Isso é deformado,’ bhikkhus, e por isso é chamado de
forma. Deformado pelo que? Deformado pelo frio, deformado pelo calor, deformado
pela fome, deformado pela sede, deformado pelo contato com as moscas,
mosquitos, vento, sol e criaturas rastejantes. ‘Isso é deformado,’
bhikkhus,<span style='mso-spacerun:yes'>  </span>por isso é chamada de forma.</span></p>



<p class=Normal><span lang=PT-BR>“E porque
chamam-na de sensação? ‘Sente,’ bhikkhus, e por isso é chamada de sensação.
Sente o que? Sente prazer, sente dor, sente nem prazer, nem dor. ‘Sente,’
bhikkhus, por isso é chamada de sensação.</span></p>



<p class=Normal><span lang=PT-BR>“E porque
chamam-na de percepção? ‘Percebe,’ bhikkhus, por isso é chamada de percepção.
Percebe o que? Percebe o azul, percebe o amarelo, percebe o vermelho, percebe o
branco. ‘Percebe,’ bhikkhus, por isso é chamada de percepção.</span></p>



<p class=Normal><span lang=PT-BR>“E porque
chamam-nas de formações volitivas? ‘Fabricam o condicionado,’ bhikkhus, por
isso são chamadas de formações volitivas. E qual é o condicionado que é
fabricado? É fabricada a forma condicionada como forma; é fabricada a sensação
condicionada como sensação; é fabricada a percepção condicionada como
percepção; são fabricadas as formações volitivas condicionadas como formações volitivas; é
fabricada a consciência condicionada como consciência. ‘Fabricam o
condicionado,’ bhikkhus, por isso são chamadas de formações volitivas.</span></p>



<p class=Normal><a name=R1></a><span
lang=PT-BR>“E porque chamam-na de consciência? ‘Conscientiza,’ bhikkhus, por isso é
chamada de consciência. Conscientiza o que? Conscientiza o azedo, conscientiza
o amargo, conscientiza o picante, conscientiza e o doce, conscientiza o
alcalino, conscientiza o não alcalino, conscientiza o salgado, conscientiza o
não salgado. ‘Conscientiza,’ bhikkhus, por isso é chamada de consciência.</span></p>



<p class=Normal><span lang=PT-BR>“E assim,
um nobre discípulo bem instruído reflete desta maneira: ‘Eu agora estou sendo
transtornado pela forma. E no passado, eu também fui transtornado pela forma da
mesma maneira que agora estou sendo transtornado pela presente forma. E se eu
buscar o prazer na forma futura, então no futuro, eu também serei<span
style='mso-spacerun:yes'>  </span>transtornado pela forma da mesma maneira como
agora estou sendo transtornado pela presente forma.’ Depois de refletir dessa
maneira, ele se torna indiferente em relação à forma passada, não busca o
prazer na forma futura e a sua prática visa o desencantamento, desapego e
cessação em relação à forma presente.</span></p>



<p class=Normal><span lang=PT-BR>“[Ele
reflete:] ‘Eu agora estou sendo transtornado pela sensação ... percepção ...
formações volitivas...consciência. Mas no passado, eu também fui transtornado pela
consciência da mesma maneira que agora estou sendo transtornado pela presente
consciência. E se eu buscar o prazer na consciência futura, então no futuro
também serei transtornado pela consciência da mesma maneira como agora estou
sendo transtornado pela presente consciência.’ Depois de refletir dessa
maneira, ele se torna indiferente em relação à consciência passada, não busca o
prazer na consciência futura, e a sua prática visa o desencantamento, desapego
e cessação em relação à presente consciência.</span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O que
