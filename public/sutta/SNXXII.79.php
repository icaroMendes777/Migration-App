<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Qualquer
consciência, quer seja do passado, do futuro ou do presente, interna ou
externa; grosseira ou sutil; inferior ou superior; próxima ou distante: toda
consciência deve ser vista como na verdade é, com correta sabedoria assim:
‘Isso não é meu, isso não sou eu, isso não é o meu eu.’ <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Esse,
bhikkhus, é chamado um nobre discípulo, que desmonta e não monta; que abandona
e não se apega; que dispersa e não ajunta; que extingue e não acende.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que,
bhikkhus, ele desmonta e não monta? Ele desmonta a forma e não a monta. Ele
desmonta a sensação … percepção … formações volitivas … consciência e não a monta.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que,
bhikkhus, ele abandona e não se apega? Ele abandona a forma e a ela não se
apega. Ele abandona a sensação … percepção … formações volitivas … consciência e a ela
não se apega.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que,
bhikkhus, ele dispersa e não ajunta? Ele dispersa a forma e não a ajunta. Ele
dispersa a sensação … percepção … formações volitivas … consciência e não a ajunta.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que,
bhikkhus ele extingue e não acende? Ele extingue a forma e não a acende. Ele
extingue a sensação … percepção … formações volitivas … consciência e não a acende.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Vendo
dessa forma, o nobre discípulo bem instruído se desencanta com a forma, se
desencanta com a sensação, se desencanta com a percepção, se desencanta com as
formações volitivas, se desencanta com a consciência. Desencantado, ele se torna
desapegado. Através do desapego a sua mente é libertada. Quando ela está
libertada surge o conhecimento: ‘Libertada.’ Ele compreende que: ‘O nascimento
foi destruído, a vida santa foi vivida, o que deveria ser feito foi feito, não
há mais vir a ser a nenhum estado.’<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Esse,
bhikkhus, é chamado um nobre discípulo que nem monta e tampouco desmonta, mas
que assim permanece ao ter desmontado; que nem abandona e tampouco se apega,
mas que assim permanece ao ter abandonado; que não dispersa e tampouco junta,
mas que assim permanece ao ter dispersado; que nem extingue e tampouco acende,
mas que assim permanece ao ter extinto.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que,
bhikkhus, ele nem monta e tampouco desmonta, mas que assim permanece ao ter
desmontado? Ele nem monta e tampouco desmonta a forma, mas assim permanece ao
ter desmontado a forma. Ele nem monta e tampouco desmonta a sensação …
percepção … formações volitivas … consciência,<span style='mso-spacerun:yes'>  </span>mas
assim permanece ao ter desmontado a consciência.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que,
bhikkhus, ele nem abandona e tampouco se apega, mas que assim permanece ao ter
abandonado? Ele nem abandona e tampouco se apega à forma, mas assim permanece
ao ao ter abandonado a forma. Ele nem abandona e tampouco se apega à sensação …
percepção … formações volitivas … consciência,<span style='mso-spacerun:yes'>  </span>mas
assim permanece ao ter abandonado a consciência.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que,
bhikkhus, ele não dispersa e tampouco junta, mas que assim permanece ao ter
dispersado? Ele não dispersa e tampouco junta a forma, mas assim permanece ao ter
dispersado a forma. Ele não dispersa e tampouco junta a sensação … percepção …
formações volitivas … consciência,<span style='mso-spacerun:yes'>  </span>mas assim
permanece ao ter dispersado a consciência.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que,
bhikkhus ele nem extingue e tampouco acende, mas que assim permanece ao ter
extinto? Ele nem extingue e tampouco acende a forma, mas assim permanece ao ter
extinto a forma. Ele nem extingue e tampouco acende a sensação … percepção …
formações volitivas … consciência,<span style='mso-spacerun:yes'>  </span>mas assim
permanece ao ter extinto a consciência.<o:p></o:p></span></p>


