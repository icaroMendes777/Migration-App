<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Agora, naquela
ocasião o Nigantha Nataputta havia chegado em Macchikasanda com uma grande
comitiva de niganthas.[<a href="#N1">1</a>] <a name=R1></a>Citta, o chefe de
família, tomou conhecimento disso e junto com um grande número de discípulos
leigos foi até o Nigantha Nataputta e ambos se cumprimentaram. Quando a
conversa cortês e amigável havia terminado ele sentou a um lado. O Nigantha
Nataputta então lhe disse: <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Chefe de
família, você tem fé no contemplativo Gotama quando ele diz: ‘Há uma
concentração sem o pensamento aplicado e sustentado, há uma cessação do
pensamento aplicado e sustentado’?” [<a href="#N2">2</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Com respeito a isso, venerável senhor, eu não me guio pela fé no
Abençoado quando ele diz: ‘Há uma concentração sem o pensamento aplicado e sustentado,
há uma cessação do pensamento aplicado e sustentado.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isso
foi dito, o Nigantha Nataputta olhou com orgulho para a sua comitiva e disse:
“Vejam isso, senhores! Quão franco é esse chefe de família, Citta! Quão honesto
e aberto! Quem pensa que o pensamento aplicado e sustentado pode ser parado
imaginaria ser capaz de capturar o vento com uma rede ou interromper a
correnteza do rio Gânges com as próprias mãos.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O que você
pensa, venerável senhor, o que é superior: o conhecimento ou a fé?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O
conhecimento, chefe de família, é superior à fé.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bem,
venerável senhor, de acordo com a minha vontade, afastado dos prazeres
sensuais, afastado das qualidades não hábeis, eu entro e permaneço no primeiro
jhana, que é caracterizado pelo pensamento aplicado e sustentado, com o êxtase e
felicidade nascidos do afastamento. Depois, de acordo com a minha vontade,
abandonando o pensamento aplicado e sustentado, eu entro e permaneço no segundo
jhana ... Depois, de acordo com a minha vontade, abandonando o êxtase
... eu entro e permaneço no terceiro jhana ... Depois, de acordo com a minha
vontade, com o completo desaparecimento da felicidade ... eu entro e permaneço
no quarto jhana.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Posto que
eu assim conheço e vejo, venerável senhor, em qual outro contemplativo ou
brâmane devo depositar fé com relação à afirmação de que há uma concentração
sem o pensamento aplicado e sustentado, uma cessação do pensamento aplicado e
sustentado?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isso
foi dito, Nigantha Nataputta olhou com desconfiança para a sua comitiva e
disse:<span style="mso-spacerun: yes">  </span>“Vejam isso, senhores! Quão
desonesto é esse chefe de família, Citta! Quão fraudulento e enganador!”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Agora
mesmo, venerável senhor, entendemos que você disse: ‘Vejam isso, senhores! Quão
franco é esse chefe de família, Citta! Quão honesto e aberto!’ – no entanto,
agora entendemos que você diz: ‘Vejam isso, senhores! Quão desonesto é esse
chefe de família, Citta! Quão fraudulento e enganador!’ Se a sua primeira
afirmação é verdadeira, venerável senhor, então a sua última afirmação é falsa,
enquanto que se a primeira afirmação é falsa, então a sua última afirmação é
verdadeira.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Além
disso, venerável senhor, estas dez questões lógicas vêm à mente. Quando você
compreender o significado delas então poderá me responder juntamente com a sua
comitiva. Uma questão, um resumo, uma resposta. Duas questões, dois resumos,
duas respostas. Três ... quatro ... cinco<span style="mso-spacerun: yes"> 
</span>.... seis ... sete ... oito ... nove ... dez questões, dez resumos, dez
respostas.”<o:p></o:p></span></p>



<p class=Normal><a name=R3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Então, Citta, o chefe de família, levantou do seu assento e partiu sem
ter pedido que o Nigantha Nataputta respondesse essas dez questões lógicas. [<a
href="#N3">3</a>]<o:p></o:p></span></p>

