<p class=Normal> "Bhikkhus, quando um bhikkhu que possui cinco fatores se utiliza de uma moradia que possui cinco fatores, então em pouco tempo, realizando por si mesmo através do conhecimento direto, aqui e agora, ele entra e permanece na libertação da mente e libertação através da sabedoria que são imaculadas com a destruição de todas as impurezas. </p>

<p class=Normal>"Como, bhikkhus, um bhikkhu possui cinco fatores? </p>

<p class=Normal> (1) "Aqui, um bhikkhu tem convicção. Ele deposita fé na iluminação do Tathagata assim: 'O Abençoado é um arahant, perfeitamente iluminado, consumado no verdadeiro conhecimento e conduta, bem-aventurado, conhecedor dos mundos, um líder insuperável de pessoas preparadas para serem treinadas, mestre de devas e humanos, desperto, sublime.'</p>

<p class=Normal> (2) "Raramente ele está enfermo ou aflito, possuindo uma boa digestão, que não é nem demasiado fria nem demasiado quente, mas média, e adequada para o esforço. </p>

<p class=Normal> (3) "Ele é honesto e sincero, e se mostra como ele na verdade é para o Mestre e os seus companheiros na vida santa. </p>

<p class=Normal> (4) "Ele permanece com a sua energia desperta para o abandono de qualidades mentais prejudiciais e para a obtenção de qualidades mentais benéficas; ele é decidido, firme no seu esforço, sem se esquivar das suas responsabilidades com relação ao cultivo de qualidades mentais benéficas. </p>

<p class=Normal> (5) "Ele é sábio, ele possui a completa compreensão da origem e cessação, que é nobre e penetrante, conduzindo à completa destruição do sofrimento. </p>

<p class=Normal>"É assim que um bhikkhu possui cinco fatores. </p>

<p class=Normal>"E como uma moradia possui cinco fatores? </p>

<p class=Normal> (6) "Aqui, a moradia não é tão distante [de um lugar para esmolar alimentos] nem tão próxima, e há um caminho para ir e retornar. </p>

<p class=Normal> (7) "Durante o dia ele não é importunado por pessoas e à noite o lugar é calmo e silencioso. </p>

<p class=Normal> (8) "Há pouco contato com moscas, mosquitos, vento, o sol ardente, e cobras. </p>

<p class=Normal> (9) "Alguém que habite essa moradia poderá obter com facilidade mantos, comida esmolada, moradia, e medicamentos.</p>

<p class=Normal> (10) "Nessa moradia habitam bhikkhus seniores que são estudados, expertos no Dhamma, expertos na Disciplina, expertos nas matrizes (matikas), conhecedores dos agamas (nikayas). De tempos em tempos ele os procura e pergunta: 'Como é isso, venerável senhor? Qual o significado disso?' Aqueles veneráveis então revelam o que não havia sido revelado, esclarecem o que estava obscuro, e dissipam as suas dúvidas com relação a vários pontos duvidosos. </p>

 <p class=Normal>"É assim que uma moradia possui cinco fatores. </p>

<p class=Normal>"Quando um bhikkhu que possui esses cinco fatores se utiliza de uma moradia que possui esses cinco fatores, então em pouco tempo, realizando por si mesmo através do conhecimento direto, aqui e agora, ele entra e permanece na libertação da mente e libertação através da sabedoria que são imaculadas com a destruição de todas as impurezas." </p>

