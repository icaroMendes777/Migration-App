<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.1. Assim
ouvi. [</span><a href="#N1"><span lang=PT-BR style='mso-ansi-language:PT-BR'>1</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R1></a>Em certa ocasião, o
Abençoado estava<span style='mso-spacerun:yes'>  </span>em Kusinara, no bosque
de árvores sal dos Mallas pouco antes do parinibbana. <o:p></o:p></span></p>



<p class=Normal><a name="T1_2"></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>1.2. Então, o venerável Ananda foi até o Abençoado e depois de
cumprimentá-lo sentou a um lado e disse: “Venerável senhor, que o Abençoado não
morra nesta pequena cidade miserável feita com paus-a-pique, cercada pela
floresta no meio de nada! Venerável senhor, há outras grandes cidades tais como
Campa, Rajagaha, Savatthi, Saketa, Kosambi ou Benares. Nesses lugares há
Khattiyas, Brâmanes e chefes de família prósperos que são devotos do Tathagata
e proverão um funeral adequado para o Tathagata.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.3.
“Ananda, não diga que aqui é uma pequena cidade miserável feita com
paus-a-pique, cercada pela floresta no meio de nada! Certa vez, Ananda houve um
rei chamado Mahasudassana que girou a roda, um monarca justo de acordo com o
Dhamma, conquistador dos quatro pontos cardeais, inconquistável, que havia
estabelecido a segurança no seu reino. E esse Rei Mahasudassana tinha nesta
mesma Kusinara, sob o nome Kusavati, a sua capital. E essa cidade tinha doze
yojanas de extensão de leste a oeste, e sete yojanas de extensão de norte a
sul. Kusavati era rica, próspera e populosa, repleta de gente e bem suprida.
Tal como a cidade dos devas Alakamanda é<span style='mso-spacerun:yes'> 
</span>rica, próspera e populosa, repleta de yakkhas e bem suprida, da mesma
forma a capital real Kusavati. E Kusavati nunca se via livre de dez tipos de
sons durante o dia ou noite: o som de elefantes, cavalos, carruagens, tambores,
caixas, alaúdes, canto, címbalos, gongos e exclamações ‘coma, beba e se
divirta’ como décimo. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.4. “A
capital real Kusavati estava cercada com sete muralhas. Uma de ouro, outra de
prata, outra de berilos, outra de cristais, outra de rubis, outra de esmeraldas
e outra com todo tipo de pedras preciosas. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.5. “Os
portões de Kusavati tinham quatro cores: dourada, prateada, a cor do berilo e
do cristal. E antes de cada portão haviam sete pilares com três ou quatro
alturas de um homem. Um era de ouro, o outro de prata, outro de berilos, outro
de cristais, outro de rubis, outro de esmeraldas e outro com todo tipo de
pedras preciosas. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.6.
“Kusavati era cercada por sete fileiras de palmeiras, dos mesmos materiais. As
árvores de ouro tinham o tronco de ouro e as folhas e frutos de prata, as
árvores de prata tinham o tronco de prata e as folhas e frutos de ouro. As
árvores de berilo tinham o tronco de berilos e as folhas e frutos de cristais,
as árvores de cristais tinham o tronco de cristais e as folhas e frutos de
berilos. As árvores de rubis tinham o tronco de rubis e as folhas e frutos de
esmeraldas, as árvores de esmeraldas tinham os troncos de esmeraldas e as
folhas e frutos de rubis, enquanto que as árvores com todo o tipo de pedras
preciosas tinham essas pedras no tronco, folhas e frutos. O som das folhas
tocadas pelo vento era agradável, delicioso, doce e embriagante, tal como o som
dos cinco tipos de instrumentos musicais tocados num concerto por músicos
hábeis e bem treinados. [<a href="#N2">2</a>] <a name=R2></a>E, Ananda, os
licenciosos e beberrões em Kusavati tinham os seus desejos apaziguados pelo som
das folhas tocadas pelo vento. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.7. “O Rei
Mahasudassana possuía os sete tesouros e os quatro tipos de poder. Quais eram
os sete tesouros? Certa vez o Rei lavou a cabeça no Uposatha do décimo quinto
dia [<a href="#N3">3</a>] <a name=R3></a>e foi até o terraço no topo do
palácio para o Uposatha, a Roda Preciosa [<a href="#N4">4</a>] <a name=R4></a>surgiu
para ele, com mil raios, com a roda, o cubo, completa em todos os aspectos. Ao
vê-la, o Rei Mahasudassana pensou: ‘Eu ouvi que quando um Rei Khattiya ungido
tiver lavado a cabeça no Uposatha do décimo quinto dia e tiver ido até o
terraço no topo do palácio para o Uposatha e ali aparecer a roda preciosa com
mil raios, com a roda, o cubo, completa em todos os aspectos, então ele se
tornará um Monarca que gira a roda. Eu sou então um Monarca que gira a roda?’ <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.8.
“Então, levantando-se do seu assento e arrumando o manto externo sobre o ombro,
o Rei tomou um vaso com água com a mão esquerda, borrifou a Roda com a mão
direita e disse: ‘Gire para adiante, boa roda preciosa; triunfe, boa roda
preciosa!’<span style='mso-spacerun:yes'>  </span>Então, a roda girou para
adiante na direção leste e o Rei a seguiu com o seu exército. Agora, em
qualquer região na qual a Roda parasse, ali o Rei estabelecia residência com o
seu exército.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.9. “E
aqueles que antes a ele se opunham na região leste vinham e diziam: ‘Venha,
grande Rei; bem vindo, grande Rei; comande, grande Rei; aconselhe, grande Rei.’
O Rei assim dizia: ‘Vocês não devem matar seres vivos; vocês não devem tomar
aquilo que não for dado; vocês não devem agir de forma imprópria em relação aos
prazeres sensuais; vocês não devem dizer mentiras; vocês não devem beber
bebidas embriagantes; sejam moderados na alimentação.’[<a href="#N5">5</a>] <a
name=R5></a>E aqueles que antes a ele se opunham na região leste se tornaram
seus súditos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.10. “Então,
a roda mergulhou no oceano do leste e emergiu outra vez. E nisso, ela girou
para adiante, na direção sul ... E aqueles que se opunham na região sul se
submeteram ao Rei. Então, a roda mergulhou no oceano do sul e emergiu outra
vez. E nisso, ela girou para adiante, na direção oeste<span
style='mso-spacerun:yes'>  </span>... E aqueles que se opunham na região oeste
se submeteram ao Rei. Então, a roda mergulhou no oceano do oeste e emergiu
outra vez. E nisso, ela girou para adiante, na direção norte ... E aqueles que
se opunham na região norte se submeteram ao Rei.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.11.
“Agora, quando a roda preciosa triunfou sobre a terra de oceano a oceano, ela
retornou para a capital real e permaneceu como que presa pelo eixo ao portão do
principal palácio do Rei Mahasudassana, como um adorno no portão do palácio
principal. Assim foi como a roda preciosa apareceu para o Rei Mahasudassana.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.12.
“Então, o elefante precioso apareceu para o Rei Mahasudassana, todo branco, com
postura sétupla, com poderes supra-humanos, voando através do espaço, o rei dos
elefantes chamado ‘Uposatha.’ Ao vê-lo, a mente do Rei teve confiança nele
assim: ‘Seria maravilhoso montar no elefante, se ele se submetesse ao
treinamento!’ Então, o elefante precioso se submeteu ao treinamento como um
fino elefante puro-sangue bem domesticado há muito tempo. E assim sucedeu que o
Rei, ao testar o elefante precioso, montava nele pela manhã e depois de
atravessar toda a terra até a margem do oceano, retornava à capital real para a
refeição matinal. Assim foi como o elefante precioso apareceu para o Rei
Mahasudassana.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.13.
“Então, o cavalo precioso apareceu para o Rei Mahasudassana, todo branco, com a
cabeça negra e lustrosa e a crina como a erva munja, com poderes supra-humanos,
voando através do espaço, o rei dos cavalos chamado ‘Valahaka’, [‘Nuvem de
Tormenta’]. Ao vê-lo, a mente do Rei teve confiança nele assim: ‘Seria
maravilhoso montar no cavalo, se ele se submetesse ao treinamento!’ Então, o
cavalo precioso se submeteu ao treinamento tal como um fino cavalo puro-sangue
bem domesticado há muito tempo. E assim sucedeu que o Rei, ao testar o cavalo
precioso, montava nele pela manhã e depois de atravessar toda a terra até a
margem do oceano, retornava à capital real para a refeição matinal. Assim foi
como o cavalo real apareceu para o Rei Mahasudassana.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.14.
“Então, a jóia preciosa apareceu para o Rei Mahasudassana. A jóia era um fino
berilo da mais pura água, com oito facetas, bem lapidada. Agora, a luminosidade
da jóia preciosa se espalhava ao redor por uma légua. E assim aconteceu que
quando o Rei estava testando a jóia preciosa, ele colocou em formação o seu
exército e instalando a jóia no topo do seu estandarte, ele saiu nas trevas e
escuridão da noite. Então, todos os [habitantes dos] vilarejos próximos
começaram o seu trabalho devido à luz, pensando que fosse dia. Assim foi como a
jóia preciosa apareceu para o Rei Mahasudassana.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.15.
“Então, a mulher preciosa apareceu para o Rei Mahasudassana, bela, atraente e
graciosa, possuindo a complexão da suprema beleza, nem muito alta nem muito
baixa, nem muito magra nem muito robusta, nem muito escura nem muito clara,
superando a beleza humana, sem alcançar a beleza divina. O toque da mulher
preciosa era tal que se igualava a um tufo de paina ou um tufo de algodão.
Quando estava frio os membros dela estavam quentes; quando estava quente os
membros dela estavam frios. Do seu corpo emanava o perfume do sândalo e da sua
boca o perfume do lótus. Ela se levantava antes do Rei e se deitava depois
dele. Ela era ávida por servir, com conduta agradável e com a linguagem doce.
Como ela nunca foi infiel ao Rei, sequer em pensamento, como poderia ela sê-lo
com o corpo? Assim foi como a mulher preciosa apareceu para o Rei
Mahasudassana.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.16.
“Então, o tesoureiro precioso apareceu para o Rei Mahasudassana. O olho divino
nascido do kamma passado se manifestou nele e através deste ele via depósitos
ocultos de tesouros, com e sem proprietários. Ele se aproximava do Rei e dizia:
‘Senhor, fique tranqüilo. Eu tomarei conta dos seus assuntos financeiros.’<span
style='mso-spacerun:yes'>  </span>E assim aconteceu quando o Rei estava
testando o tesoureiro precioso, ele embarcou num navio e saindo pelo rio
Gânges, no meio da correnteza ele disse para o tesoureiro precioso: ‘Eu preciso
de ouro e lingotes, tesoureiro.’ – ‘Então, senhor, deixe que o barco seja
dirigido para uma das margens.’ – ‘Tesoureiro, na verdade é aqui que preciso de
ouro e lingotes.’ Então, otesoureiro precioso mergulhou ambas as mãos na água e
ergueu um pote cheio de ouro e lingotes e disse para o Rei: ‘Isso é o
suficiente, senhor? O oferecido é suficiente, o ofertado é suficiente?’ – ‘Isso
é suficiente, tesoureiro, o suficiente foi feito, o suficiente foi ofertado.’
Assim foi como o tesoureiro precioso apareceu para o Rei Mahasudassana.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.17.
“Então, o conselheiro precioso apareceu para o Rei Mahasudassana, sábio,
esperto e sagaz, capaz de fazer com que o Rei promovesse aquilo que fosse digno
de ser promovido, de descartar aquilo que devesse ser descartado e de
estabelecer aquilo que devesse ser estabelecido. Ele se aproximava do Rei e
dizia: ‘Senhor, fique tranqüilo. Eu o aconselharei.’ Assim foi como o
conselheiro precioso apareceu para o Rei Mahasudassana. Esses eram os sete
tesouros que o Rei Mahasudassana possuía.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.18.
“Novamente, Ananda, o Rei possuía quatro poderes. [<a href="#N6">6</a>] <a
name=R6></a>Quais eram eles? Primeiro, o Rei era belo, atraente e elegante,
possuindo a complexão da beleza suprema, e ele superava todos os outros homens
nesse aspecto.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.19.
“Segundo, o Rei vivia por muito tempo superando todos os outros homens nesse
aspecto. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.20.
“Terceiro, ele estava livre das enfermidades e aflições, possuindo boa digestão
que não era nem muito fria, nem muito quente, superando todos os outros homens
nesse aspecto.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.21.
“Quarto, ele era querido e estimado pelos brâmanes e chefes de família. Como um
pai é querido e estimado pelos seus filhos, assim também o Rei era querido e
estimado pelos brâmanes e chefes de família. Brâmanes e chefes de família
também eram queridos e estimados pelo Rei. Como os filhos são queridos e estimados
pelo pai, assim também brâmanes e chefes de família eram queridos e estimados
pelo Rei. Certa vez o Rei estava num parque das delícias com o seu exército
completo. Então, os brâmanes e chefes de família foram até ele e disseram o
seguinte: ‘Senhor, conduza devagar para que possamos vê-lo por mais tempo.’ E
assim ele disse para o seu cocheiro: ‘Cocheiro, conduza mais devagar para que
eu possa ver os brâmanes e chefes de família por mais tempo.’ Esses são os
quatro tipos de poder que o Rei Mahasudassana possuía.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.22.
“Então, o Rei Mahasudassana pensou: ‘E se eu construísse piscinas com flores de
lótus entre as palmeiras?’ E assim ele fez. As piscinas foram revestidas com
azulejos de quatro cores, ouro, prata, berilo e cristal, cada piscina tendo
quatro escadas, uma de ouro, outra de prata, outra de berilo e outra de
cristal. E a escada de ouro tinha quatro pilares de ouro com corrimão e
balaústres de prata, a escada de prata tinha quatro pilares de prata e corrimão
e balaústres de ouro, e assim por diante. E as piscinas com flores de lótus
tinham dois tipos de grade, ouro e prata – as grades de ouro tinham pilares de
ouro, corrimão e balaústres de prata e as grades de prata tinham pilares de
prata, corrimão e balaústres de ouro.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.23.
“Então, o Rei pensou: ‘E se eu abastecesse cada piscina com as flores adequadas
para fazer grinaldas – flores de lótus azuis, amarelas, vermelhas e brancas que
durassem por todas as estações sem desbotar?’ E assim ele fez. Então ele
pensou: ‘E se eu colocasse banhistas na beira das piscinas para banhar aqueles
que lá fossem?’ E assim ele fez. Então, ele pensou: ‘E se eu instalasse postos
de caridade próximos às piscinas de modo que aqueles que necessitam comida
possam comer, aqueles que necessitam bebida possam beber, aqueles que
necessitam roupas possam se vestir, aqueles que necessitam transporte possam
ser transportados, aqueles que necessitam descanso possam descansar, aqueles
que necessitam uma esposa possam se casar e aqueles que necessitam de moedas de
ouro possam obtê-las?’ E assim ele fez. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.24.
“Então, os Brâmanes e chefes de família obtiveram muita riqueza e foram até o
Rei e disseram: ‘Senhor, aqui está a riqueza que juntamos especialmente para o
Rei, por favor aceite-a!’ ‘Obrigado, amigos, mas eu tenho riqueza suficiente
proveniente das rendas de acordo com as leis. Fiquem com essa riqueza e além
disso, aceitem um pouco mais!’ Tendo sido dessa forma recusados pelo Rei, eles
se reuniram num canto e consideraram: ‘Não seria correto que levássemos essa
riqueza de volta para casa. E se nós construíssemos uma moradia para o Rei
Mahasudassana.’ Então, eles foram até o Rei e disseram: ‘Senhor, nós
construiremos uma moradia para você,’ e o Rei concordou em silêncio. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.25. “Entã,
Sakka, o senhor dos devas, compreendendo com a sua mente o pensamento na mente
do Rei Mahasudassana, disse para o criado deva Vissakamma: ‘Venha, amigo
Vissakamma, construa uma moradia para o Rei Mahasudassana, um palácio chamado
Dhamma.’ ‘Muito bem, Senhor, Vissakamma respondeu, e com a mesma rapidez com
que um homem forte pode estender o seu braço flexionado ou flexionar o seu
braço estendido ele desapareceu do paraíso do Trinta e Três e apareceu na
frente do Rei Mahasudassana, dizendo: ‘Senhor, eu construirei uma moradia para
você, um palácio chamado Dhamma.’ O Rei concordou em silêncio e<span
style='mso-spacerun:yes'>  </span>Vissakamma construiu o Palácio do Dhamma.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.26. “O
Palácio do Dhamma, Ananda, tinha uma légua de extensão de leste a oeste e meia
légua de extensão de norte a sul. Todo o palácio até o equivalente à altura de
três homens estava recoberto com azulejos de quatro cores: ouro, prata, berilo
e cristal e ele continha oitenta e quatro mil colunas das mesmas cores. O
palácio tinha vinte quatro escadarias com as mesmas quatro cores e as
escadarias de ouro tinham pilares de ouro com corrimão e balaústres de prata
... (igual ao verso 23). O palácio também tinha oitenta e quatro mil cômodos
com as mesmas cores. No cômodo de ouro havia um sofá de prata, no cômodo de
prata havia um sofá de ouro, no cômodo de berilo havia um sofá de marfim e no
cômodo de cristal um sofá de sândalo. Na porta do cômodo de ouro havia a
representação de uma palmeira de prata, com o tronco de prata, folhas e frutos
de ouro ... Na porta do cômodo de prata havia a representação de uma palmeira
de ouro, com o tronco de ouro, folhas e frutos de prata, na porta do cômodo de
berilo havia a representação de uma palmeira de cristal, com o tronco de
cristal, folhas e frutos de berilo, na porta do cômodo de cristal havia a
representação de uma palmeira de berilo, com tronco de berilo, folhas e frutos
de cristal.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.27.
“Então, o Rei pensou: ‘E seu eu fizesse um bosque de palmeiras todas de ouro em
frente à porta do grande salão com cumeeira onde passo os dias?’ E assim ele
fez.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.28. “Em
volta do Palácio do Dhamma havia duas grades, uma de ouro, outra de prata. A de
ouro tinha pilares de ouro, corrimão e balaústres de prata e a grade de prata
tinha pilares de prata, corrimão e balaústres de ouro.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.29. “O
Palácio do Dhamma estava circundado por duas carreiras com sinos. Uma carreira
era de ouro com sinos de prata, a outra de prata com sinos de ouro. E quando as
carreiras eram tocadas pelo vento o som produzido era agradável, delicioso,
doce e embriagante, tal como o som dos cinco tipos de instrumentos musicais
tocados num concerto por músicos hábeis e bem treinados. E os licenciosos e
beberrões em Kusavati tinham os seus desejos apaziguados pelo som dos sinos
tocados pelo vento. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.30.
“Quando o Palácio do Dhamma foi concluído, era difícil de olhar para ele,
ofuscava os olhos, como no último mês da estação das chuvas, no outono, quando
o céu está claro e sem nuvens, é difícil manter os olhos no sol rompendo a
névoa, assim também era o Palácio do Dhamma depois de concluído.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.31.
“Então, o Rei pensou: ‘E se eu construísse uma piscina com flores de lótus
chamada Dhamma em frente ao Palácio do Dhamma?’ E assim ele fez. A piscina
tinha uma légua de extensão de leste a oeste e meia légua de extensão de norte
a sul, e foi revestida com azulejos de quatro cores, ouro, prata, berilo e
cristal, com quatro escadas, uma de ouro, outra de prata, outra de berilo e
outra de cristal. E a escada de ouro tinha quatro pilares de ouro ... (igual ao
verso 22).<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.32. “A
piscina do Dhamma estava circundada por sete tipos de palmeiras. O som
produzido pelas folhas tocadas pelo vento era agradável, delicioso, doce e
embriagante, tal como o som dos cinco tipos de instrumentos musicais tocados
num concerto por músicos hábeis e bem treinados. E, Ananda, os licenciosos e
beberrões em Kusavati tinham os seus desejos apaziguados pelo som dos sinos
tocados pelo vento.<span style='mso-spacerun:yes'>  </span><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.33.
“Quando o Palácio do Dhamma e a Piscina do Dhamma haviam sido concluídos, o Rei
Mahasudassana, tendo satisfeito todos os desejos daqueles que na época eram
contemplativos ou Brâmanes, ou venerados como tais, foi para o Palácio do
Dhamma.”<o:p></o:p></span></p>



<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>[Fim da
primeira recitação]</b><o:p></o:p></span></b></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.1.
“Então, o Rei Mahasudassana pensou: ‘De qual kamma é o fruto, de qual kamma é o
resultado, o fato de eu agora ser tão forte e poderoso?’ Então, ele pensou:
‘Esse é o fruto de três tipos de kamma: da generosidade, do autocontrole e da
contenção” [</span><a href="#N7"><span lang=PT-BR style='mso-ansi-language:
PT-BR'>7</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=R7></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2.2. “Então, o Rei foi até o grande salão com cumeeira e parando na
porta exclamou: ‘Que o pensamento de cobiça cesse! Que o pensamento de má
vontade cesse! Que o pensamento de crueldade cesse! Que não ocorram mais
pensamentos de cobiça, má vontade ou crueldade!’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.3.
“Então, o Rei entrou no grande salão com cumeeira e sentou com as pernas
cruzadas num sofá de ouro e afastado dos prazeres sensuais, afastado das
qualidades não hábeis, ele entrou e permaneceu no primeiro jhana, que é
caracterizado pelo pensamento aplicado e sustentado, com o êxtase e felicidade
nascidos do afastamento. Abandonando o pensamento aplicado e sustentado, ele
entrou e permaneceu no segundo jhana, que é caracterizado pela segurança interna e 
perfeita unicidade da mente, sem o pensamento aplicado e sustentado, 
com o êxtase e felicidade nascidos da concentração. Abandonando o êxtase, ele entrou e permaneceu
 no terceiro jhana que é caracterizado pela felicidade sem o êxtase, acompanhada pela atenção plena, 
plena consciência e equanimidade, acerca do qual os nobres declaram: ‘Ele permanece numa estada feliz, 
equânime e plenamente atento.’ Com o completo desaparecimento da felicidade, ele entrou e permaneceu no quarto jhana, que possui nem felicidade nem sofrimento, com a atenção plena e a equanimidade purificadas.
<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.4.
“Então, o Rei, saiu do grande salão com cumeeira e foi para o salão com
cumeeira de ouro e sentou com as pernas cruzadas num sofá de prata e permaneceu
permeando o primeiro quadrante com a mente imbuída de amor bondade, da mesma
forma o segundo, da mesma forma o terceiro, da mesma forma o quarto; assim
acima, abaixo, em volta e em todos os lugares, para todos bem como para si
mesmo, ele permaneceu permeando o mundo todo com a mente imbuída de amor
bondade, abundante, transcendente, imensurável, sem hostilidade e sem má
vontade. E ele fez o mesmo com a mente imbuída de compaixão, alegria altruísta
e equanimidade. [</span><a href="#N8"><span lang=PT-BR style='mso-ansi-language:
PT-BR'>8</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=R8></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2.5. “O Rei Mahasudassana tinha oitenta e quatro mil cidades e a capital
Kusavati era a principal, ele tinha oitenta e quatro mil palácios e o Palácio
do Dhamma era o principal; ele tinha oitenta e quatro mil salões com cumeeira e
o grande salão com cumeeira era o principal; ele tinha oitenta e quatro mil
sofás de ouro, prata, marfim, sândalo, cobertos com colchas felpudas, coberta com colchas de lã branca, colchas
bordadas, peles de antílope e gamo, coberta com um baldaquino e com almofadas
vermelhas para a cabeça e os pés; ele tinha oitenta e quatro mil
elefantes adornados com ornamentos de ouro, com estandartes de ouro e cobertos
com redes de ouro, Uposatha o elefante real era o principal; ele tinha oitenta
e quatro mil carruagens cobertas com peles de leão, peles de tigre, peles de
leopardos ou com tecidos alaranjados adornados com ornamentos de ouro, com estandartes
de ouro e cobertas com redes de ouro, a carruagem Vejayanta era a principal;
ele tinha oitenta e quatro mil jóias sendo que a jóia preciosa era a principal;
ele tinha oitenta e quatro mil esposas sendo que a Rainha Subhadda era a
principal; ele tinha oitenta e quatro mil chefes de família sendo que o chefe
de família precioso era o principal; ele tinha oitenta e quatro mil empregados
Khattiya sendo que o conselheiro precioso era o principal; ele tinha oitenta e
quatro mil vacas com ubres como fina juta e baldes de prata; ele tinha oitenta
e quatro mil fardos de roupas feitas do mais fino linho, algodão, seda e lã;
ele tinha oitenta e quatro mil oferendas de arroz que ali estavam para
alimentar os necessitados, noite e dia. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.6.
“Naquela época, os oitenta e quatro mil elefantes do Rei Mahasudassana o
serviam noite e dia. E ele pensou: ‘Esses oitenta e quatro mil elefantes me
servem noite e dia. E se ao final de cada século quarenta e dois mil elefantes
me servissem e fizessem um rodízio?’ E ele deu instruções nesse sentido para o
conselheiro precioso e assim foi feito. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.7. “E,
Ananda, depois de muitas centenas, centenas de milhares de anos, a Rainha
Subhadda pensou: ‘Já faz muito tempo que não vejo o Rei Mahasudassana. E se eu
fosse vê-lo?’ Assim ela disse para as suas aias: ‘Venham agora, lavem as
cabeças e vistam roupas limpas. Já faz muito tempo que não vimos o Rei
Mahasudassana. Devemos ir vê-lo.’ ‘Sim, Majestade’, elas responderam e se
arrumaram e depois foram ter com a Rainha. A Rainha Subhadda disse para o
conselheiro precioso: ‘Amigo conselheiro, reúna o exército com quatro divisões.
Já faz muito tempo que não vimos o Rei Mahasudassana. Devemos ir vê-lo.’ ‘Muito
bem, Majestade’, o conselheiro precioso respondeu e tendo reunido o exército, ele
comunicou à Rainha: ‘Agora é o momento Majestade, faça como julgar
adequado.’<span style='mso-spacerun:yes'>         </span><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.8.
“Então, a Rainha Subhadda com o exército com quatro divisões e mais as suas
aias foi até o Palácio do Dhamma e depois de entrar foi até o grande salão com
cumeeira e parou encostada ao umbral da porta. E o Rei Mahasudassana, pensando:
‘Que barulho tão forte, como de uma multidão?’ saiu por uma porta e viu a Rainha
Subhadda encostada ao umbral da porta. Ele disse: ‘Fique aí, Rainha! Não
entre!’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.9.
“Então, o Rei Mahasudassana disse para um certo homem: ‘Aqui, rapaz, vá até o
grande salão com cumeeira, traga para fora o sofá de ouro e coloque-o entre as
palmeiras de ouro.’ ‘Muito bem, senhor’, o homem disse, e assim fez. Então, o
Rei Mahasudassana se deitou no seu lado direito, na postura do leão com um pé
sobre o outro, atento e plenamente consciente. [<a href="#N9">9</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R9></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2.10. “Então, a Rainha Subhadda pensou: ‘As faculdades do Rei
Mahasudassana estão serenas, a sua complexão está límpida e brilhante, Oh! – espero
que ele não esteja morto.’[<a href="#N10">10</a>] <a name=R10></a>Assim ela
disse: ‘Senhor, das suas oitenta e quatro mil cidades, Kusavati é a principal.
Faça um desejo, desperte o desejo em viver lá!’ Dessa forma, recordando-o de
todas as suas posses reais (igual ao verso 2.5) ela o exortou a desejar
continuar vivo.<span style='mso-spacerun:yes'>  </span><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.11. “Em
vista disso, o Rei Mahasudassana disse para a Rainha: ‘Durante muito tempo,
Rainha, você disse palavras agradáveis, deliciosas, prazerosas para mim, mas
agora neste momento as suas palavras são desagradáveis, ruins, desprazerosas.’
‘Senhor, o que então devo falar?’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>‘Assim é
como você deve falar: “Todas as coisas prazerosas e sedutoras estão sujeitas à
mudança, ao desaparecimento, tornando-se diferentes. Senhor, não morra cheio de
desejos. Morrer cheio de desejos é doloroso e censurável. Das sua oitenta e
quatro mil cidades, Kusavati é a principal: abandone o desejo, abandone a
cobiça em viver lá ... Dos seus oitenta e quatro mil palácios, o Palácio do
Dhamma é o principal: abandone o desejo, abandone a cobiça em viver lá ...” (e
assim por diante, igual ao verso 2.5)<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.12. “Em
vista disso, a Rainha Subhadda gritou e desatou a chorar. Depois, limpando as
lágrimas, ela disse: ‘Senhor, todas as coisas prazerosas e sedutoras estão
sujeitas à mudança ... Senhor, não morra cheio de desejos.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.13.
“Pouco tempo depois, o Rei Mahasudassana morreu e da mesma forma que um chefe
de família ou o seu filho podem se sentir letárgicos depois de uma deliciosa
refeição, assim foi a sensação que ele sentiu ao falecer, e ele teve um
renascimento favorável no mundo de Brahma.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Durante
oitenta e quatro mil anos o Rei Mahasudassana brincou com jogos infantis,
durante oitenta e quatro mil anos ele atuou como vice-regente; durante oitenta
e quatro mil anos ele governou o reino; durante oitenta e quatro mil anos, como
um leigo ele viveu a vida santa no Palácio do Dhamma. E tendo praticado as
quatro moradas puras, ao morrer, com a dissolução do corpo ele renasceu no
mundo de Brahma. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.14.
“Agora, Ananda, você poderá pensar assim: ‘Com certeza, uma outra pessoa era o
Rei Mahasudassana naquela ocasião.’ Mas essa não deve ser a interpretação. Era
eu o Rei Mahasudassana naquela ocasião. Aquelas oitenta e quatro mil cidades,
entre as quais Kusavati era a principal, eram minhas ... as<span
style='mso-spacerun:yes'>  </span>oitenta e quatro mil oferendas de arroz ...
eram minhas.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.15. “E
daquelas oitenta e quatro mil cidades eu habitava apenas uma, Kusavati, ... das
oitenta e quatro mil esposas que eu tinha, apenas uma cuidava de mim, e elas se
chamavam Khattiyani ou Velamikani;[<a href="#N11">11</a>] <a name=R11></a>dos
oitenta e quatro mil fardos de roupas eu tinha apenas um ...; das oitenta e
quatro mil oferendas de arroz havia apenas uma medida que eu comia.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.16. “Você
vê, Ananda, como todos esses estados condicionados do passado mudaram e
desapareceram! Portanto, Ananda, os estados condicionados são impermanentes,
eles são instáveis, eles não nos confortam, e em sendo esse o caso, Ananda, nós
não deveríamos nos regozijar nos estados condicionados, nós deveríamos deixar
de ter interesse neles e libertarmo-nos deles. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.17. “Seis
vezes, Ananda, eu me recordo de haver descartado o corpo neste lugar e na
sétima vez eu o descartei como um Monarca que girou a roda, um monarca justo de
acordo com o Dhamma, conquistador dos quatro pontos cardeais, inconquistável,
que havia estabelecido a segurança no meu reino e que possuía sete tesouros.
Mas, Ananda, eu não vejo nenhum lugar neste mundo, com os seus devas, maras e
Brahmas ou nesta população, com os seus contemplativos e Brâmanes, príncipes e
povo, onde o Tathagata irá pela oitava vez descartar o corpo.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o
que disse o Abençoado. Tendo dito isso, o Mestre disse mais:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Impermanentes
são as coisas condicionadas, sujeitas à origem e cessação. Tendo surgido, elas são
destruídas, a sua cessação é a verdadeira bem-aventurança.”<o:p></o:p></span></p>



