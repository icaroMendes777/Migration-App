<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Amigos, é porque eu desenvolvi e cultivei os quatro fundamentos da
atenção plena que alcancei a supremacia do conhecimento direto. Além disso,
amigos, é porque eu desenvolvi e cultivei os quatro fundamentos da atenção
plena que exerço os vários tipos de poderes supra-humanos: tendo sido um, me
torno vários; tendo sido vários, me torno um; apareço e desapareço; cruzo sem
nenhum problema uma parede, um cercado, uma montanha ou através do espaço;
mergulho e saio da terra como se fosse água; caminho sobre a água sem afundar
como se fosse terra; sentado de pernas cruzadas cruzo o espaço como se fosse um
pássaro; com a minha mão toco e acaricio a lua e o sol tão forte e poderoso; <a
name=R29></a>exerço poderes corporais até mesmo nos distantes mundos de
Brahma.”<o:p></o:p></span></p>

