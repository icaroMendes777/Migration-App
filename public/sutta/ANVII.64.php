<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“<a
name=worthy>Um bhi</a>kkhu dotado com estas sete qualidades é merecedor de
dádivas, merecedor de hospitalidade, merecedor de oferendas, merecedor de
saudações com reverência, um campo inigualável de mérito para o mundo. Quais
sete? É o caso em que um bhikkhu tem noção do Dhamma, noção do significado,
noção de si mesmo, noção de moderação, noção do tempo, noção de encontros
sociais e noção das diferenças entre os indivíduos. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E
como um bhikkhu tem noção do Dhamma? É o caso em que um bhikkhu conhece o
Dhamma: sumários, prosa e verso, análises, versos, exclamações, símiles, histórias de vidas passadas, eventos maravilhosos, perguntas e respostas (a classificação mais antiga dos ensinamentos
do Buda). Se ele não conhecesse o Dhamma - sumários, prosa e verso, análises, versos, exclamações, símiles, histórias de vidas passadas, eventos maravilhosos, perguntas e respostas – não se
diria que ele é alguém com noção do Dhamma. Portanto é porque ele conhece o
Dhamma – sumários ... perguntas e respostas – que se diz que ele é
alguém com noção do Dhamma. Ele tem a noção do Dhamma. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como um bhikkhu tem
noção do significado? É o caso em que um bhikkhu compreende o significado desta
e daquela afirmação – ‘Este é o significado daquela afirmação, aquele é o
significado desta.’ Se ele não compreendesse o significado desta e daquela
afirmação - ‘Este é o significado daquela afirmação, aquele é o significado
desta.’ - não se diria que ele é alguém com noção do significado. Portanto é
porque ele compreende o significado desta e daquela afirmação – ‘Este é o significado
daquela afirmação, aquele é o significado desta.’ - que se diz que ele é alguém
com noção do significado. Ele tem noção do Dhamma e noção do significado. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como um bhikkhu tem
noção de si mesmo? É o caso em que um bhikkhu conhece a si mesmo. ‘Este é o
tanto que tenho de convicção, virtude, tolerância, sabedoria, rapidez de
compreensão.’ Se ele não conhecesse a si mesmo – ‘Este é o tanto que tenho de
convicção, virtude, tolerância, sabedoria, rapidez de compreensão’ – não se
diria que ele é alguém com noção de si mesmo. Portanto é porque ele conhece a
si mesmo – ‘Este é o tanto que tenho de convicção, virtude, tolerância,
sabedoria, rapidez de compreensão’ – que se diz que ele é alguém com noção de
si mesmo. Ele tem noção do Dhamma, noção do significado e noção de si mesmo. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como um bhikkhu tem
noção de moderação? É o caso em que um bhikkhu
compreende a moderação ao aceitar mantos, comida esmolada, moradia e
medicamentos. Se ele não compreendesse a moderação ao aceitar mantos, comida
esmolada, moradia e medicamentos, não se diria que ele é alguém com noção de
moderação. Portanto é porque ele compreende a moderação ao aceitar mantos,
comida esmolada, moradia e medicamentos, que se diz que ele é alguém com noção
de moderação. Ele tem noção do Dhamma, noção do significado, noção de si mesmo
e noção de moderação.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como um bhikkhu tem
noção do tempo? É o caso em que um bhikkhu compreende o tempo: ‘Este, é o
momento para recitação; este, é o momento para questionar; este, é o momento de
fazer esforço (na meditação); este, é o momento para isolamento.’ Se ele não
compreendesse o tempo - ‘Este, é o momento para recitação; este, é o momento
para questionar; este, é o momento de fazer esforço (na meditação); este, é o
momento para isolamento’ – não se diria que ele é alguém com noção do tempo.
Portanto é porque ele compreende o tempo - ‘Este, é o momento para recitação;
este, é o momento para questionar; este, é o momento de fazer esforço (na
meditação); este, é o momento para isolamento’ – que se diz que ele é alguém
com noção do tempo. Ele tem noção do Dhamma, noção do significado, noção de si
mesmo, noção de moderação e noção do tempo.<o:p></o:p></span></p>

<p class=Normal><a name=manners><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como um
bhikkhu tem noção de encontros sociais</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>? É o caso em que um bhikkhu compreende o
encontro social em que está: ‘Este, um encontro social de Khattiyas;
este, um encontro social de brâmanes; este, um encontro social de chefes de
família; este, um encontro social de contemplativos; aqui se deve aproximar
deles desta forma, ficar em pé desta forma, agir desta forma, sentar desta
forma, falar desta forma, permanecer em silêncio desta forma.’ Se ele não
compreendesse o encontro social em que está - ‘Este, um encontro social de
Khattiyas; este, um encontro social de brâmanes; este, um encontro
social de chefes de família; este, um encontro social de contemplativos; aqui
se deve aproximar deles desta forma, ficar em pé desta forma, agir desta forma,
sentar desta forma, falar desta forma, permanecer em silêncio desta forma’ –
não se diria que ele é alguém com noção de encontros sociais. Portanto porque
ele compreende o encontro social em que está - ‘Este, um encontro social de
Khattiyas; este, um encontro social de brâmanes; este, um encontro
social de chefes de família; este, um encontro social de contemplativos; aqui
se deve aproximar deles desta forma, ficar em pé desta forma, agir desta forma,
sentar desta forma, falar desta forma, permanecer em silêncio desta forma’ –
que se diz que ele é alguém com noção de encontros sociais. Ele tem noção do Dhamma,
noção do significado, noção de si mesmo, noção de moderação, noção do tempo e
noção de encontros sociais. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como um bhikkhu tem
noção das diferenças entre os indivíduos? É o caso em que as pessoas são
conhecidas pelo bhikkhu em termos de duas categorias: <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“De duas pessoas – uma quer
ver os nobres e a outra não quer – aquela que não quer ver os nobres deve ser
criticada por isso, aquela que quer ver os nobres deve ser elogiada por isso.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“De duas pessoas que querem
ver os nobres – uma quer ouvir o verdadeiro Dhamma e a outra não quer – aquela
que não quer ouvir o verdadeiro Dhamma deve ser criticada por isso, aquela que
quer ouvir o verdadeiro Dhamma deve ser elogiada por isso. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“De duas pessoas que querem
ouvir o verdadeiro Dhamma – uma ouve com o ouvido atento e a outra ouve sem o
ouvido atento – aquela que ouve sem o ouvido atento deve ser criticada por
isso, aquela que ouve com o ouvido atento deve ser elogiada por isso. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“De duas pessoas que ouvem
com o ouvido atento – uma, tendo ouvido o Dhamma, se recorda do que ouviu e a
outra não se recorda – aquela que tendo ouvido o Dhamma não se recorda do que
ouviu deve ser criticada por isso, aquela que tendo ouvido o Dhamma se recorda
do que ouviu deve ser elogiada por isso. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“De duas pessoas que tendo
ouvido o Dhamma, se recordam do que ouviram – uma investiga o significado do
Dhamma que memorizou e a outra não investiga – aquela que não investiga o
significado do Dhamma que memorizou deve ser criticada por isso, aquela que
investiga o significado do Dhamma que memorizou deve ser elogiada por isso. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“De duas pessoas que
investigam o significado do Dhamma que memorizaram – uma pratica o Dhamma de
acordo com o Dhamma, tendo noção do Dhamma, tendo noção do significado, e outra
que não pratica – aquela que não pratica o Dhamma de acordo com o Dhamma, tendo
noção do Dhamma, tendo noção do significado, deve ser criticada por isso,
aquela que pratica o Dhamma de acordo com o Dhamma, tendo noção do Dhamma,
tendo noção do significado deve ser elogiada por isso. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“De duas pessoas que
praticam o Dhamma de acordo com o Dhamma, tendo noção do Dhamma, tendo noção do
significado – uma pratica tanto para o seu benefício como para o benefício dos
outros, e outra pratica para o seu próprio benefício porém não para o benefício
dos outros - aquela que pratica para o seu próprio benefício porém não para o
benefício dos outros deve ser criticada por isso, aquela que pratica tanto para
o seu benefício como para o benefício doa outros deve ser elogiada por isso. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Assim é como as pessoas
são conhecidas pelo bhikkhu em termos de duas categorias. E assim é como um
bhikkhu tem noção das diferenças entre os indivíduos. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Um bhikkhu dotado com
estas sete qualidades é merecedor de dádivas, merecedor de hospitalidade,
merecedor de oferendas, merecedor de saudações com reverência, um campo
inigualável de mérito para o mundo.”<o:p></o:p></span></p>

