<p class=Normal><a name=N14></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[14] </span><i><span lang=PT-BR
style='mso-ansi-language:PT-BR'>Viññanam anidassanam, anantam sabbato pabham</span></i><span
lang=PT-BR style='mso-ansi-language:PT-BR'>. <span style='mso-bidi-font-weight:
bold'>MA toma o sujeito da sentença, consciência (<i>viññana),</i> como sendo
Nibbana.<br>
 Thanissaro Bhikkhu oferece “sem superfície” ao invés de “sem
atributos” como tradução alternativa para </span><i>anidassanam, </i>tomando
como base o símile empregado pelo Buda no </span><a href="SNXII.64.php#simile"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>SN XII.64</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>. Essa consciência é sem superfície
porque se a cobiça pelas bases dos sentidos externas for eliminada a consciência não
tem onde “pousar” e dessa forma se torna não estabelecida. Isso não quer dizer
que a consciência é aniquilada mas simplesmente que, como a luz do sol no
símile mencionado, ela não tem localização, ela não pode mais ser definida, ela
se encontra além da dualidade de tempo e espaço. Nesta mesma vida ela não pode
ser localizada ou definida em relação a nenhum dos agregados; após a morte ela
não pode ser definida como existindo, não existindo, nenhum dos dois, ou ambos,
porque as descrições apenas se aplicam ao que pode ser definido. <span
style='mso-bidi-font-weight:bold'><br>
 Uma tradução alternativa de
Ajaan Amaro para esta frase: </span><i>anidassanam: </i><span style='mso-bidi-font-style:
italic'>vazia, invisível ou desprovida de sinais; <i>anantam: </i>ilimitada,
não confinada, infinita; <i><span style='mso-spacerun:yes'> </span>sabbato
pabham: </i>luminosa em todas as direções, acessível por todos os lados.<br>
</span><span
style='mso-bidi-font-weight:bold'> Bhikkhu Ñanananda no livro <i
style='mso-bidi-font-style:normal'>Concept and Reality</i> interpreta <i
style='mso-bidi-font-style:normal'>viññanam anidassanam</i> como “a
consciência que nada manifesta.” Em que a manifestação diz respeito aos conceitos, ou seja na consciência do arahant os conceitos não se manifestam, todos os conceitos se tornaram transparentes.<br>Estes versos também aparecem como parte de
uma estrofe no <a href="DN11.php#T85">DN 11.85</a> [<a href="#R14">Retorna</a>]<o:p></o:p></span></span></p>


