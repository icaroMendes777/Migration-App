<hr size=1 width="100%" align=center>

<p class="Normal"><span lang=PT-BR mso-ansi-language:PT-BR'>O Anguttara Nikaya, é a Quarta divisão do Sutta Pitaka, consistindo de suttas organizados em onze seções <i>(nipatas)</i> de acordo com o seu conteúdo numérico. Por exemplo, o primeiro <i>nipata </i>– o "Livro dos Um" – contém suttas que dizem respeito a um só tópico; o segundo <i>nipata </i> – o "Livro dos Dois" – contém suttas que dizem respeito a duas coisas (ex: um sutta acerca de tranqüilidade e insight; um outro acerca das duas pessoas que jamais poderão retribuir de maneira adequada (aos seu pais); um outro acerca dos dois tipos de felicidade; etc.); o terceiro <i>nipata </i> contém suttas que dizem respeito a três coisas (ex: um sutta acerca dos três tipos de ações que merecem elogios; outro acerca dos três tipos de ofensas), e assim por diante.</span></p>

<p class="Normal"><span lang=PT-BR mso-ansi-language:PT-BR'>À primeira vista essa classificação pode parecer um tanto quanto pedante e presunçosa, mas na verdade é muito útil. Por exemplo, se você vagamente se recorda de ter ouvido alguém dizer algo acerca dos cinco temas que merecem contemplação diária, e você quiser buscar onde se encontra esse trecho no Cânone, uma boa idéia é começar a busca pelo "Livro dos Cinco" no Anguttara.</span></p>

