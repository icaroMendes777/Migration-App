<p class=Normal>Assim ouvi. Em certa ocasião, o Abençoado estava viajando em Malla com uma grande sangha de bhikkhus, chegando até a cidade de Pava. Lá ele ficou no mangueiral de Cunda, o ferreiro.</p>



<p class=Normal>Cunda, o ferreiro, ouviu que o Abençoado havia chegado em Pava e que estava no seu mangueiral. Então, ele foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e o Abençoado instruiu, motivou, estimulou e encorajou Cunda com um discurso do Dhamma.</p>



<p class=Normal>Então, Cunda disse: “Que o Venerável Gotama concorde em aceitar amanhã uma refeição, junto com a Sangha dos bhikkhus!” E o Abençoado concordou em silêncio.</p>



<p class=Normal>Então, sabendo que o Abençoado havia concordado, Cunda levantou do seu assento e depois de homenagear o Abençoado, mantendo-o à sua direita, partiu.</p>



<p class=Normal>Quando a noite havia quase terminado, Cunda fez com que se preparassem vários tipos de boa comida, com fartura de ‘delícias de porco’, e anunciou para o Abençoado que a refeição estava pronta. Quando tudo estava pronto eles anunciaram a hora para o Abençoado: “É hora, Mestre Gotama, a refeição está pronta.”</p>



<p class=Normal>Então, o Abençoado vestiu os seus mantos e, carregando a sua tigela e o manto externo, foi com a Sangha dos bhikkhus para a casa de Cunda, sentando num assento que havia sido preparado ele disse: “Sirva a ‘delícias de porco’ que foi preparada para mim e sirva o restante da boa comida para os bhikkhus.” - “Muito bem, venerável senhor,” Cunda disse e assim fez.</p>



<p class=Normal>Então, o Abençoado disse para Cunda: “O que tiver sobrado da ‘delícias de porco’ você deve enterrá-lo numa cova, porque, Cunda, eu não vejo ninguém neste mundo com os seus devas, maras e Brahmas, nesta população com os seus contemplativos e Brâmanes, seus príncipes e povo, que ao comer isso possa ser capaz de digeri-lo, exceto o Tathagata. “Muito bem, venerável senhor”, disse Cunda e tendo enterrado o que havia sobrado da ‘delícias de porco’ numa cova ele foi até o Abençoado e depois de cumprimentá-lo sentou a um lado. Então, o Abençoado, depois de instruí-lo, motivá-lo, estimulá-lo e encorajá-lo com um discurso do Dhamma, levantou do seu assento e partiu.</p>



<p class=Normal>E logo depois do Abençoado ter comido a refeição dada por Cunda, o ferreiro, uma doença terrível se abateu sobre ele, com diarréia sangrenta e dores terríveis como se estivesse a ponto de morrer. Porém, o Abençoado suportou tudo com atenção plena e plena consciência, sem se queixar. Então, o Abençoado disse: “Ananda, vamos para Kusinara” E o venerável Ananda respondeu “Sim, venerável senhor.”</p>



	<p class=Normal style='margin-left:.75in'>Tendo comido a refeição de Cunda, assim dizem,<br> 

	o Sábio sofreu com enfermidade grave, dolorosa, mortal.</p>

	

	<p class=Normal style='margin-left:.75in'>Tendo ingerido aquele prato de ‘delícias de porco’ <br>

	a severa doença se apoderou do Mestre.<br> 

	Tendo se recuperado, o Abençoado disse:<br> 

	“Agora irei para Kusinara.”</p>



<p class=Normal>Então saindo da estrada, o Abençoado foi até o pé de uma árvore e disse: “Venha, Ananda, dobre um manto em quatro para mim, estou cansado e quero sentar.” - “Muito bem, venerável senhor,” e Ananda assim fez.</p>



<p class=Normal>O Abençoado sentou no assento que havia sido preparado e disse: “Ananda, traga-me um pouco de água, tenho sede e quero beber.”</p> 



<p class=Normal>Ananda respondeu: “Venerável senhor, quinhentas carroças passaram por esta estrada. A água foi perturbada pelas rodas e não está boa, ela está suja e turva. Mas, venerável senhor, o rio Kukuttha que está próximo tem água limpa, agradável, fresca, pura, com margens aprazíveis, deliciosas. Lá o Abençoado poderá beber a água e refrescar os membros.”</p>



<p class=Normal>Uma segunda vez o Abençoado disse: “Ananda, traga-me um pouco de água, tenho sede e quero beber.” </p>



<p class=Normal>Uma segunda vez, Ananda respondeu: “Venerável senhor, quinhentas carroças passaram por esta estrada. A água foi perturbada pelas rodas e não está boa, ela está suja e turva. Mas, venerável senhor, o rio Kukuttha que está próximo tem água limpa, agradável, fresca, pura, com margens aprazíveis, deliciosas. Lá o Abençoado poderá beber a água e refrescar os membros.”</p>



<p class=Normal>Uma terceira vez o Abençoado disse: “Ananda, traga-me um pouco de água, tenho sede e quero beber.”</p>



<p class=Normal>“Muito bem, venerável senhor,” Ananda disse e tomando a tigela foi até o rio. E aquele rio, cujas águas haviam sido perturbadas pelas rodas, que não estavam boas, que estavam sujas e turvas, à medida que Ananda se aproximava, começaram a fluir puras, límpidas e imaculadas.</p>



<p class=Normal>E o venerável Ananda pensou: “Maravilhosos, admiráveis são os grandes poderes do Tathagata! Essas águas foram perturbadas pelas rodas …, e à medida que me aproximei começaram a fluir puras, límpidas e imaculadas!” Ele colocou a água na tigela e a levou para o Abençoado e disse o que ele estava pensando, acrescentando: “Que o Abençoado beba a água, que o Iluminado beba!” E o Abençoado bebeu a água.</p>



<p class=Normal>Então, o Abençoado foi com uma grande comitiva de bhikkhus para o rio Kukuttha. Ele entrou na água, se banhou, bebeu e ao sair foi para o mangueiral onde disse para o venerável Cundaka: ‘Venha, Cundaka, dobre uma manto em quatro para mim. Estou cansado e quero me deitar.” - “Muito bem, venerável senhor,” Cundaka disse e assim fez.</p>



<p class=Normal>Então, o Abençoado deitou no seu lado direito, na postura do leão, com um pé sobre o outro, atento e plenamente consciente, após anotar na mente o horário para despertar. E o venerável Cundaka sentou em frente ao Abençoado.</p>



	<p class=Normal style='margin-left:.75in'>O Buda foi para o rio Kukuttha<br> 

	com as águas límpidas, brilhantes e agradáveis,<br> 

	lá o Mestre mergulhou o corpo cansado.<br> 

	O Tathagata, sem igual no mundo.</p> 

	

	<p class=Normal style='margin-left:.75in'>Cercado pelos bhikkhus dos quais ele era o cabeça,<br>

	o Mestre Abençoado, Protetor do Dhamma,<br>

	o Grande Sábio foi para o mangueiral.</p>

	

	<p class=Normal style='margin-left:.75in'>Ele se dirigiu ao bhikkhu Cundaka, dizendo: <br>

	“Deitarei num manto dobrado em quatro.”<br>

	E assim solicitado pelo grande Experto,<br>

	Cundaka arrumou o manto em quatro.<br>

	O Mestre deitou os membros cansados para descansar<br>

	enquanto Cundaka sentava-se à sua frente.</p>



<p class=Normal>Então, o Abençoado disse para o venerável Ananda: “Pode acontecer, Ananda, que Cunda, o ferreiro, sinta remorso, pensando: ‘É minha culpa, é devido à minha falha que o Tathagata morreu depois da última refeição oferecida por mim!’ Caso haja remorso em Cunda este deve ser dissipado desta forma: ‘Esse é o seu mérito, Cunda, a sua boa ação, que o Tathagata tenha realizado o parinibbana depois da última refeição oferecida por você, pois, amigo Cunda. Eu ouvi da própria boca do Abençoado que essas duas oferendas de comida produzem grandes frutos, grandes benefícios, mais frutuosas e vantajosas do que quaisquer outras. Quais duas? Aquela refeição depois da qual o Tathagata realiza a perfeita iluminação, a outra depois da qual ele realiza o parinibbana. Essas duas oferendas são mais frutuosas e vantajosas do que todas as demais. A sua ação, Cunda, conduz a uma vida longa, a uma bela aparência, à felicidade, à fama, ao paraíso e à senhoria.” Dessa forma, Ananda, o remorso de Cunda deve ser dissipado.</p>



<p class=Normal>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p>



	<p class=Normal style='margin-left:.75in'>“Com a prática da generosidade<br> 

	o mérito se incrementa.<br> 

	Com a contenção<br> 

	 a raiva é controlada.</p>

	

	<p class=Normal style='margin-left:.75in'>Aquele que é hábil<br> 

	e abandona as coisas prejudiciais,<br>

	erradicando a cobiça, a raiva e a delusão<br>

	encontra em nibbana um fim.”</p>

