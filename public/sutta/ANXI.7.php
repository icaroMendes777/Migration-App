<p class=Normal>Então o venerável  Ananda foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse:  “Venerável senhor, um bhikkhu poderia alcançar uma tal concentração de modo que ele não teria a percepção da terra em relação à terra, nem da água em relação à água, nem do fogo … do ar … da base do espaço infinito ... da base da consciência infinita ... da base do nada ... da base da nem percepção, nem não percepção ... deste mundo ... nem do mundo além em relação ao mundo além; qualquer coisa  ouvida, sentida, conscientizada, buscada, procurada, ponderada pela mente, e ainda assim seria perceptivo?” </p>

<p class=Normal> “Sim, Ananda, um bhikkhu poderia.” </p>

<p class=Normal> “Mas como, venerável senhor, um bhikkhu poderia alcançar uma tal concentração?” </p>

<p class=Normal> “É o caso, Ananda, em que um bhikkhu tem a percepção deste modo: ‘Isto é a paz, isto é o sublime, isto é, o silenciar de todas as formações, o abandono de todas as aquisições, a destruição do desejo, desapego, cessação, nibbana.’ É desse modo que um bhikkhu pode alcançar uma tal concentração da mente de modo que ele não tenha a percepção da terra em relação à terra, nem da água em relação à água, nem do fogo … do ar … da base do espaço infinito ... da base da consciência infinita ... da base do nada ... da base da nem percepção, nem não percepção ... deste mundo ... nem do mundo além, em relação ao mundo além; qualquer coisa  ouvida, sentida, conscientizada, buscada, procurada, ponderada pela mente, e ainda assim seria perceptivo.” </p>

<p class=Normal>Então o venerável Ananda. tendo ficado contente e satisfeito com as palavras do Abençoado, levantou do seu assento, e depois de homenagear o Abençoado, mantendo-o à sua direita, foi até o venerável Sariputta ambos se cumprimentaram. Quando a conversa cortês e amigável havia terminado ele sentou a um lado e disse para o venerável Sariputta: </p>

<p class=Normal>"Amigo Sariputta, um bhikkhu poderia alcançar uma tal concentração da mente de modo que ele não teria a percepção da terra em relação à terra, nem da água em relação à água, nem do fogo … do ar … da base do espaço infinito ... da base da consciência infinita ... da base do nada ... da base da nem percepção, nem não percepção ... deste mundo ... nem do mundo além em relação ao mundo além; qualquer coisa  ouvida, sentida, conscientizada, buscada, procurada, ponderada pela mente, e ainda assim seria perceptivo?” </p>

<p class=Normal> “Sim, amigo Ananda, um bhikkhu poderia.” </p>

<p class=Normal> “Mas como, amigo Sariputta, um bhikkhu poderia alcançar uma tal concentração da mente?” </p>

<p class=Normal> “É o caso, amigo Ananda, em que um bhikkhu tem a percepção deste modo: ‘Isto é a paz, isto é o sublime, isto é, o silenciar de todas as formações, o abandono de todas as aquisições, a destruição do desejo, desapego, cessação, nibbana.’ É desse modo que um bhikkhu pode alcançar uma tal concentração da mente de modo que ele não tenha a percepção da terra em relação à terra, nem da água em relação à água, nem do fogo … do ar … da base do espaço infinito ... da base da consciência infinita ... da base do nada ... da base da nem percepção, nem não percepção ... deste mundo ... nem do mundo além, em relação ao mundo além; qualquer coisa  ouvida, sentida, conscientizada, buscada, procurada, ponderada pela mente, e ainda assim seria perceptivo.” </p>

<p class=Normal>"É admirável e maravilhoso, amigo, que o significado e fraseado de ambos mestre e discípulo coincidam e estejam de acordo e não divergem em relação ao estado supremo (nibbana). Exatamente agora, amigo, fui até o Abençoado e perguntei sobre esse assunto. O Abençoado respondeu exatamente com os mesmos termos e frases empregados pelo venerável Sariputta. É admirável e maravilhoso, amigo, que o significado e fraseado de ambos mestre e discípulo coincidam e estejam de acordo e não divergem em relação ao estado supremo." </p>



