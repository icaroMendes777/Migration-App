<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>21. <i>Pupilos</i><o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Se, Oh senhor, você agora aprova<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>esse ensinamento do Iluminado,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>nós também seguiremos a vida santa sob ele,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>este homem com sublime sabedoria.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>22. <i>Sela</i><o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Aqui há trezentos brâmanes<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>que com as mãos erguidas imploram:<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>‘Oh que nós possamos viver a vida santa<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>sob você, Oh Abençoado.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>23. <i>Buda</i><o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“A vida santa é bem proclamada,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Oh Sela,” disse o Abençoado,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>“para ser vista aqui e não diferida;<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>aquele que treina com diligência<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>encontrará frutos na vida santa.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>24. Então, o
brâmane Sela e a sua assembléia receberam a admissão na vida santa sob o
Abençoado e eles receberam a admissão completa.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>25. Então,
quando havia terminado a noite, Keniya fez com que se preparassem vários tipos
de boa comida no seu próprio eremitério e fez com que se anunciasse a hora para
o Abençoado: “É hora, Mestre Gotama, a refeição está pronta.” Então, ao
amanhecer, o Abençoado </span> se vestiu e carregando a sua tigela e
o manto externo<span lang=PT-BR style='mso-ansi-language:PT-BR'>, foi com a
Sangha dos bhikkhus até o eremitério de Keniya e sentou num assento que havia
sido preparado. Então, com as próprias mãos Keniya serviu e satisfez a Sangha
dos bhikkhus liderada pelo Buda com os vários tipos de boa comida. </span>Em
seguida, quando<span lang=PT-BR style='mso-ansi-language:PT-BR'> o Abençoado </span>havia
terminado de comer e retirado a mão da sua tigela<span lang=PT-BR
style='mso-ansi-language:PT-BR'>, Keniya </span>sentou a um lado, num assento
mais baixo<span lang=PT-BR style='mso-ansi-language:PT-BR'>. O Abençoado então deu
a sua bênção com estes versos: <o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>26. “Oferendas queimadas são a glória do fogo,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Savitri a glória dos mantras dos Vedas,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>a glória dos seres humanos, um rei,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>a glória dos rios, o mar;<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>a lua é a glória das estrelas,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>o sol é a glória de tudo que brilha;<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>mérito é a glória de todos que aspiram;<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>a Sangha, é a glória dos generosos.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Depois que
o Abençoado deu a sua bênção com esses versos, ele levantou do seu assento e
partiu.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>27. Então,
p</span>ermanecendo sós, isolados, diligentes, ardentes e decididos, em pouco
