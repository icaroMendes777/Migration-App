<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Oito dias se passaram, Aquele que tudo vê, <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>desde que buscamos o seu refúgio,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>nestas sete noites, Oh Abençoado, <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>fomos domesticados no seu ensinamento. <o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
