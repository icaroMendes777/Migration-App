<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião, o Abençoado estava perambulando pela região de
Anguttarapa com uma grande Sangha de bhikkhus, com mil duzentos e cinqüenta
bhikkhus e por fim ele acabou chegando numa cidade chamada Apana.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. Keniya,
o contemplativo com o cabelo emaranhado e sujo ouviu:<span style="mso-spacerun:
yes">  </span>“Gotama o contemplativo, o filho dos Sakyas, que adotou a vida
santa deixando o clã dos Sakyas, anda perambulando pelo país dos Anguttarapas e
chegou em Apana com um grande número de bhikkhus, com mil duzentos e cinqüenta
bhikkhus. E acerca desse mestre Gotama existe essa boa reputação ... (igual ao <a
href="MN91.php#T3">MN 91.3</a>) ... É bom poder encontrar alguém tão nobre.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. Então,
Keniya, o contemplativo com o cabelo emaranhado e sujo foi até o Abençoado e
ambos se cumprimentaram, quando a conversa amigável e cortês havia terminado,
ele sentou a um lado. O Abençoado o instruiu, motivou, estimulou e encorajou
com um discurso do Dhamma. Então, depois de ter sido instruído, motivado,
estimulado e encorajado pelo Abençoado com um discurso do Dhamma, Keniya disse
para o Abençoado: “Que o Mestre Gotama junto com a Sangha dos bhikkhus concorde
em aceitar a refeição de amanhã oferecida por mim.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isso
foi dito o Abençoado respondeu: “A Sangha dos bhikkhus é grande, Keniya, consistindo
de mil duzentos e cinqüenta bhikkhus e você deposita total confiança nos
brâmanes.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Uma segunda
vez Keniya disse para o Abençoado: “Embora a Sangha dos bhikkhus seja grande,
Mestre Gotama, consistindo de mil duzentos e cinqüenta bhikkhus, e embora eu
deposite total confiança nos brâmanes, apesar disso, que o Mestre Gotama junto
com a Sangha dos bhikkhus concorde em aceitar a refeição de amanhã oferecida
por mim.” Uma segunda vez o Abençoado respondeu:<span style="mso-spacerun:
yes">  </span>“A Sangha dos bhikkhus é grande, Keniya ...”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Uma
terceira vez Keniya disse para o Abençoado: “Embora a Sangha dos bhikkhus seja
grande, Mestre Gotama ... apesar disso que o Mestre Gotama junto com a Sangha
dos bhikkhus concorde em aceitar a refeição de amanhã oferecida por mim.” O
Abençoado concordou em silêncio.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. Então,
sabendo que o Abençoado havia concordado, Keniya levantou do seu assento e foi
até o seu próprio eremitério onde ele se dirigiu aos seus amigos e
companheiros, seus pares e parentes da seguinte forma: “Ouçam-me senhores, meus
amigos e companheiros, meus pares e parentes. O contemplativo Gotama foi
convidado por mim para a refeição de amanhã junto com a Sangha dos bhikkhus.
Façam as compras e os preparativos necessários para mim.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
senhor,” eles responderam, e alguns escavaram fornos, alguns cortaram madeira,
alguns lavaram os utensílios, alguns prepararam jarros com água, alguns
prepararam os assentos, enquanto que o próprio Keniya erigiu um pavilhão. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. Agora,
naquela ocasião o brâmane Sela estava vivendo em Apana. Ele era um mestre dos
três Vedas com os seus mantras, liturgia, fonologia e etimologia e as histórias
como quinto elemento; hábil em filologia e gramática, um perito em filosofia
natural e nas marcas de um grande homem, e ele estava ensinando a recitação dos
mantras para trezentos estudantes brâmanes.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. Naquela
época, Keniya havia depositado total confiança no brâmane Sela. Então o brâmane
Sela enquanto caminhava e perambulava fazendo exercício acompanhado pelos seus
trezentos estudantes brâmanes, chegou ao eremitério de Keniya. Lá ele viu
alguns homens escavando fornos, alguns cortando madeira, alguns lavando
utensílios, alguns preparando jarros com água, alguns preparando assentos,
enquanto que o próprio Keniya estava erigindo um pavilhão. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. Ao ver
aquilo, ele perguntou para Keniya: “O que está acontecendo? O Mestre Keniya está
se casando ou organizando um casamento? Ou haverá um grande sacrifício? Ou o
Rei Seniya Bimbisara de Magadha com uma grande comitiva foi convidado para a
refeição de amanhã?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. “Eu não
estou me casando ou organizando um casamento, Mestre Sela, nem o Rei Seniya
Bimbisara de Magadha com uma grande comitiva foi convidado para a refeição de
amanhã, mas estou planejando um grande sacrifício. O contemplativo Gotama, o
filho dos Sakyas, que adotou a vida santa deixando o clã dos Sakyas, andava
perambulando pelo país dos Anguttarapas e chegou em Apana com um grande número
de bhikkhus, com mil duzentos e cinqüenta bhikkhus. E acerca desse mestre
Gotama existe essa boa reputação: ‘Esse Abençoado é um arahant,
perfeitamente iluminado, consumado no verdadeiro conhecimento e conduta,
bem-aventurado, conhecedor dos mundos, um líder insuperável de pessoas
preparadas para serem treinadas, mestre de devas e humanos, desperto, sublime<span
lang=PT-BR style='mso-ansi-language:PT-BR'>.’ Ele foi convidado por mim para a
refeição de amanhã junto com a Sangha dos bhikkhus.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. “Você
disse ‘Um arahant,
perfeitamente iluminado,’ Keniya?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu disse
‘Um arahant,
perfeitamente iluminado’ Sela.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Você disse
‘Um arahant,
perfeitamente iluminado’ Keniya?’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu disse
‘Um arahant,
perfeitamente iluminado’ Sela.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. Então
ocorreu ao brâmane Sela: “Mesmo a palavra ‘Um arahant,
perfeitamente iluminado’ é difícil de ser encontrada
neste mundo. Agora as trinta e duas marcas de um Grande Homem foram
transmitidas através de gerações por meio dos nossos mantras e o Grande Homem
que as possui tem apenas dois possíveis destinos, nenhum outro. [<a href="#N1">1</a>]
<a name=R1></a> Se ele viver a
vida em família ele se tornará um Monarca que gira a roda, um monarca justo que
governará de acordo com o Dhamma, conquistador dos quatro pontos cardeais,
inconquistável, que estabelecerá a segurança no seu reino e que possuirá os sete
tesouros. Que são: a Roda Preciosa, o Elefante Precioso, o
Cavalo Precioso, a Jóia Preciosa, a Mulher Preciosa, o Tesoureiro Precioso e como sétimo o
Conselheiro Precioso. [<a href="#N2">2</a>] <a name=R2></a> Ele terá mais de mil filhos que serão
corajosos e heróicos e que aniquilarão os exércitos inimigos. Ele governará,
tendo conquistado esta terra circundada pelo mar, sem bastão ou espada, através
do Dhamma. Mas se ele deixar a vida em família e seguir a vida santa, então ele
se tornará um arahant, um Buda perfeitamente iluminado, aquele que remove o véu
do mundo.” [<a href="#N3">3</a>]
<a name=R3></a><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. [Ele
disse]: “Estimado Keniya, onde está agora o Mestre Gotama, um arahant,
perfeitamente iluminado?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isso
foi dito, Keniya estendeu o seu braço direito e disse: “Lá, onde se encontra
aquela linha verde do bosque, Mestre Sela.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. Então, o
brâmane Sela com os seus trezentos estudantes brâmanes foram até o Abençoado.
Ele se dirigiu aos estudantes brâmanes assim: “Venham em silêncio, senhores,
caminhem com cuidado; pois os Abençoados são difíceis de serem abordados, eles
perambulam sós como os leões. Quando eu estiver falando com o contemplativo
Gotama, não interfiram e não me interrompam, mas esperem até que a nossa
conversa termine.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. Então,
o brâmane Sela foi até o Abençoado e ambos se cumprimentaram. Depois que a
conversa amigável e cortês havia terminado, ele sentou a um lado e procurou
pelas trinta e duas marcas de um Grande Homem no corpo do Abençoado. Ele viu
mais ou menos as trinta e duas marcas de um Grande Homem no corpo do Abençoado,
exceto duas; ele ficou em dúvida e não pôde ter certeza sobre duas dessas
marcas, e não pôde chegar a uma conclusão sobre elas: sobre a genitália contida
numa bainha e sobre o tamanho da língua. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então,
ocorreu ao Abençoado que: “Este brâmane Sela vê mais ou menos as trinta e duas
marcas de um Grande Homem no meu corpo, exceto duas; ele tem dúvida e incerteza
sobre duas dessas marcas e não pode chegar a uma conclusão sobre elas: sobre a
genitália contida numa bainha e sobre o tamanho da língua.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14. Então,
o Abençoado através dos seus poderes supra-humanos fez com que o brâmane Sela
pudesse ver a sua genitália contida numa bainha. <a name=R5></a>Em seguida, o
Abençoado esticou a língua para fora, lambendo ambas as orelhas e ambas as
narinas e depois cobriu toda<span style="mso-spacerun: yes">  </span>a extensão
da sua testa com a língua.<o:p></o:p></span></p>



<p class=Normal><a name=T15></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>15. Então o brâmane Sela pensou: “O contemplativo Gotama está provido
com as trinta e duas marcas de um Grande Homem; elas estão completas, não
incompletas. Mas eu não sei se ele é um arahant,
perfeitamente iluminado, ou não. No entanto, eu ouvi dos
brâmanes anciãos, que falam de acordo com a casta dos mestres, que aqueles que
são arahants, perfeitamente iluminados, se revelam como tal quando são louvados.
E se eu, cara a cara, louvasse o contemplativo Gotama com versos apropriados.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, cara
a cara, ele louvou o Abençoado com versos apropriados:<o:p></o:p></span></p>



