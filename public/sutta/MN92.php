<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Você é um leão livre dos apegos,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>você abandonou o medo e o terror.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Aqui estes trezentos bhikkhus em pé<br>

<span lang=PT-BR style='mso-ansi-language:
