<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Notas:<o:p></o:p></span></b></p>


<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] O comentário explica que há dois tipos de confinamento, (<i
style='mso-bidi-font-style:normal'>sambadha</i>): confinamento devido aos cinco
obstáculos e confinamento devido aos prazeres sensuais. A passagem, (<i
style='mso-bidi-font-style:normal'>okasa</i>), é um nome para jhana. No <a href="ANIX.42.php">AN IX.42</a> o Ven. Ananda explica o confinamento e a passagem de
modo sequencial: primeiro os prazeres sensuais são chamados de confinamento e o
primeiro jhana é a passagem; depois <i style='mso-bidi-font-style:normal'>vitakka-vicara</i>
são o confinamento e o segundo jhana a passagem e assim por diante, culminando
com a destruição das impurezas, (<i style='mso-bidi-font-style:normal'>asava)</i>,
como passagem final. [<a href="#R1">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[2] Ajaan Brahmavaso explica que: “A afirmação de que o Buda descobriu
os jhanas também aparece no <a href="ANIX.42.php">AN IX.42</a>. No <a href="DN10.php">Subha Sutta</a>, o Buda afirma que ‘E eu
não vejo essa nobre concentração sendo realizada dessa forma por nenhum outro
contemplativo ou Brâmane das outras escolas.’ Os suttas <a href="SNXLV.14.php">SN
XLV.14</a> e 15 dizem que os Jhanas não ‘surgem, exceto com o surgimento de um
Tathagata, um Arahant, um Perfeitamente Iluminado.’ Esses enunciados canônicos,
e além desses há muitos mais, dão forte respaldo à afirmação de que o Buda descobriu
os jhanas. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Há várias razões
que sugerem que os primeiros mestres do Bodisatva, Alara Kalama e Udaka
Ramaputta, não ensinavam os jhanas. O argumento mais convincente é a afirmação do
Buda de que seja o que for que esses mestres ensinavam, isso “não conduzia ao
desencantamento, ao desapego, à cessação, à paz, ao conhecimento direto, à
iluminação, a Nibbana” (<a href="MN26.php#T15">MN 26.15</a> e mais os suttas <a
href="MN52.php">MN 52</a>, <a href="MN64.php">MN 64</a> e a <a href="ANIX.3.php">AN IX.3</a>). É óbvio que
aquilo que os primeiros mestres do Bodistava afirmavam ser as ‘realizações imateriais’
não poderiam ser autênticas.” <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Outra
evidência que suporta o comentário feito por Ajaan Brahm pode ser encontrada no
<a href="SNXLI.8.php">SN XLI.8</a>, no qual o líder dos Jainistas, Nigantha Nataputta,
duvida que seja possível existir uma concentração sem o pensamento aplicado e
sustentado, demonstrando que os contemporâneos do Buda desconheciam os
verdadeiros jhanas. [<a href="#R2">Retorna</a>]<o:p></o:p></span></p>



