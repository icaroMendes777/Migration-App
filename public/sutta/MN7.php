<p class=Normal><a name=N3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[3] <i>Cittassa upakkilesa</i>. A palavra <i>upakkilesa</i> é usada
algumas vezes com o sentido de imperfeições na meditação de
concentração, tal como no <a href="MN128.php#T27">MN 128.27, 30</a>; algumas
vezes com o sentido de imperfeições no insight, tal como no
Vsm XX. 105; e algumas vezes significando corrupções que surgem
à partir das três raízes prejudiciais – desejo, raiva e delusão – nessas formas
ou nos seus desdobramentos. Neste sutta é usado com esse terceiro significado. [</span><a href="#R3"><span lang=PT-BR
style='mso-ansi-language:PT-BR'>Retorna</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=N4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[4] MA faz várias tentativas para fazer uma distinção entre a cobiça (<i>abhijjha</i>)
e o desejo inábil (<i>visamalobha</i>), mas depois indica que como do ponto de
vista do treinamento superior, toda cobiça é ruim, os dois termos podem ser
compreendidos como nomes diferentes para o mesmo fator mental, cobiça ou
paixão. [</span><a href="#R4"><span lang=PT-BR style='mso-ansi-language:PT-BR'>Retorna</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>]</span><span lang=PT-BR
style='font-size:11.0pt;mso-bidi-font-size:12.0pt;mso-ansi-language:PT-BR'><o:p></o:p></span></p>


<p class=Normal><a name=N5></a> [5] </span><span lang=PT-BR
style='mso-ansi-language:PT-BR'>MA diz que o abandono do qual se fala neste
caso deve ser compreendido como o “abandono através da erradicação” (<i>samucchedappahana</i>),
isto é, o desenraizamento completo através do caminho supramundano. As
dezesseis corrupções são abandonadas pelos nobres caminhos na seguinte ordem:<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. O
caminho de ‘entrar na correnteza”: desprezo, insolência, inveja, avareza,
dissimulação, trapaça.<o:p></o:p></span></p>

<p class=Normal ><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2. O caminho de ‘não retorno’: má vontade, raiva, rancor, negligência.<span
style='mso-tab-count:1'>                                                                              </span><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. O
caminho de arahant: cobiça e desejo inábil, teimosia,
rivalidade, presunção, arrogância, vaidade. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>MA mantém,
referindo-se a uma exegese antiga, que neste trecho o caminho daquele ‘que não
retorna’ está sendo descrito. Portanto, devemos compreender que aquelas
corrupções abandonadas pelo caminho do arahant foram até este ponto
abandonadas apenas em parte, em suas manifestações mais grosseiras. [</span><a
