<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[2] Um destino infeliz (<i>duggati</i>) é o renascimento numa das
esferas de privação – inferno, reino animal e o reino dos fantasmas. Um destino
feliz (<i>sugati</i>), mencionado um pouco abaixo, é o renascimento num estado
superior, no mundo dos seres humanos ou nos mundos dos devas.[</span><a href="#R2"><span
