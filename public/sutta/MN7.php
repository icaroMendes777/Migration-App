<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. [<a href="#N1">1</a>] <a name=R1></a>Em certa ocasião o Abençoado estava
em Savatthi, no Bosque de Jeta, no Parque de Anathapindika.<a name=T2></a> Lá
ele se dirigiu aos monges desta forma: “Bhikkhus,” – “Venerável senhor,” eles
responderam. O Abençoado disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.
“Bhikkhus, suponham que um pano estivesse sujo e manchado e um tintureiro o
mergulhasse em um corante, quer seja azul ou amarelo ou vermelho ou rosa; o
pano ficaria mal tingido e com a cor impura. Por que isso? Devido à contaminação
do pano. Da mesma forma, quando a mente é impura, um destino infeliz pode ser
esperado. [</span><a href="#N2"><span lang=PT-BR style='mso-ansi-language:PT-BR'>2</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R2></a>Bhikkhus, suponham que
um pano estivesse limpo e claro e um tintureiro o mergulhasse num corante, quer
seja azul ou amarelo ou vermelho ou rosa; o pano ficaria bem tingido e com a
cor pura. Por que isso? Devido à pureza do pano. Da mesma forma, quando a mente
é pura, um destino feliz pode ser esperado.<o:p></o:p></span></p>



<p class=Normal><a name=T3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>3. “Quais, bhikkhus, são as corrupções que contaminam a mente? [</span><a
href="#N3"><span lang=PT-BR style='mso-ansi-language:PT-BR'>3</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R3></a>Cobiça e os desejos
inábeis são corrupções que contaminam a mente. [</span><a href="#N4"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>4</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>] <a name=R4></a>Má vontade … raiva … rancor
… desprezo … insolência … inveja … avareza … dissimulação … trapaça …
teimosia … rivalidade … presunção … arrogância … vaidade … negligência … é
uma corrupção que contamina a mente.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style="mso-spacerun: yes"> </span><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. “Sabendo
que a cobiça e os desejos inábeis são corrupções que contaminam a mente, um
bhikkhu os abandona. [</span><a href="#N5"><span lang=PT-BR style='mso-ansi-language:
PT-BR'>5</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>] <a
name=R5></a>Sabendo que a má vontade … negligência é uma corrupção que
contamina a mente, um bhikkhu a abandona.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style="mso-spacerun: yes"> </span><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. “Quando
um bhikkhu compreendeu que a cobiça e os desejos inábeis são corrupções que
contaminam a mente e os<span style="mso-spacerun: yes">  </span>abandonou,
quando um bhikkhu compreendeu que a má vontade ... negligência é uma corrupção
que contamina a mente e a abandonou, ele adquire perfeita claridade, serenidade e confiança no Buda
assim: [</span><a href="#N6"><span lang=PT-BR style='mso-ansi-language:PT-BR'>6</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R6></a>‘O Abençoado é
um arahant, perfeitamente iluminado, consumado no verdadeiro conhecimento e
conduta, bem-aventurado, conhecedor dos mundos, um líder insuperável de pessoas
preparadas para serem treinadas, mestre de devas e humanos, desperto,
sublime.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. “Ele
adquire perfeita claridade, serenidade e confiança no Dhamma assim: ‘O Dhamma é bem proclamado pelo
Abençoado, visível no aqui e agora, com efeito imediato, que convida ao exame,
que conduz para adiante, para ser experimentado pelos sábios por eles mesmos.’<o:p></o:p></span></p>



<p class=Normal><a name=T7></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>7. “Ele adquire perfeita claridade, serenidade e confiança na Sangha assim: ‘A Sangha dos
discípulos do Abençoado pratica o bom caminho, pratica o caminho reto, pratica
o caminho verdadeiro, pratica o caminho adequado, isto é, os quatro pares de
pessoas, os oito tipos de indivíduos; esta Sangha dos discípulos do Abençoado é
merecedora de dádivas, merecedora de hospitalidade, merecedora de oferendas,
merecedora de saudações com reverência, um campo inigualável de mérito para o
mundo.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. “Quando,
em parte, [</span><a href="#N7"><span lang=PT-BR style='mso-ansi-language:PT-BR'>7</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R7></a>ele abriu mão,
expulsou, libertou, abandonou e abdicou das corrupções da mente,<span
style="mso-spacerun: yes">  </span>ele considera o seguinte:<br> ‘Eu possuo
perfeita claridade, serenidade e confiança no Buda’ e ele obtém inspiração do significado, obtém
inspiração do Dhamma, [</span><a href="#N8"><span lang=PT-BR style='mso-ansi-language:
PT-BR'>8</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>] <a
name=R8></a>obtém satisfação conectada com o Dhamma. Estando satisfeito, o êxtase surge nele;
naquele que está em êxtase, o corpo fica calmo; naquele, cujo corpo
está calmo, sente felicidade; naquele que sente felicidade, a mente fica
concentrada. [</span><a href="#N9"><span lang=PT-BR style='mso-ansi-language:
PT-BR'>9</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=R9></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>9. “Ele considera o seguinte: ‘Eu possuo perfeita claridade, serenidade e confiança no Dhamma’ e
ele obtém inspiração do significado, obtém inspiração do Dhamma, obtém satisfação
conectada com o Dhamma. Estando satisfeito ... a mente fica concentrada. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. “Ele
considera o seguinte: ‘Eu possuo perfeita claridade, serenidade e confiança na Sangha’ e ele obtém
inspiração do significado, obtém inspiração do Dhamma, obtém satisfação
conectada com o Dhamma. Estando satisfeito ... a mente fica concentrada.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. “Ele
considera o seguinte: ‘Em parte, eu abri mão, expulsei, libertei, abandonei e
abdiquei as corrupções da mente’ e ele obtém inspiração do significado, obtém
inspiração do Dhamma, obtém satisfação conectada com o Dhamma. Estando satisfeito,
o êxtase surge nele; naquele que está em êxtase, o corpo fica calmo;
naquele, cujo corpo está calmo, sente felicidade; naquele que sente felicidade, a
mente fica concentrada.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12.
“Bhikkhus, se um bhikkhu com tal virtude, tal concentração e tal
sabedoria [</span><a href="#N10"><span lang=PT-BR style='mso-ansi-language:
PT-BR'>10</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>] <a
name=R10></a>comesse comida esmolada consistindo de arroz de primeira com
vários tipos de molhos e vários tipos de caril, até mesmo isso não seria um
obstáculo para ele. [</span><a href="#N11"><span lang=PT-BR style='mso-ansi-language:
PT-BR'>11</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>] <a
name=R11></a>Tal como um pano sujo e manchado se torna puro e brilhante com a
ajuda de água limpa ou como o ouro se torna puro e brilhante com a ajuda de uma
fornalha, da mesma forma, se um bhikkhu com tal virtude ... comesse comida
esmolada ... isso não seria um obstáculo para ele.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. “</span>Ele
permanece com o coração pleno de amor bondade, permeando o primeiro quadrante com a mente imbuída de amor bondade<span
lang=PT-BR style='mso-ansi-language:PT-BR'>, [</span><a href="#N12"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>12</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>] <a name=R12></a>da mesma forma o segundo, da
mesma forma o terceiro, da mesma forma o quarto; assim acima, abaixo, em volta
e em todos os lugares, para todos bem como para si mesmo, </span>ele permanece
permeando o mundo todo com a mente imbuída de amor bondade, abundante, <span
lang=PT-BR style='mso-ansi-language:PT-BR'>transcendente</span>, imensurável,
sem hostilidade e sem má vontade<span lang=PT-BR style='mso-ansi-language:PT-BR'>.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14-16. “</span>Ele
permanece com o coração pleno de compaixão, permeando o primeiro quadrante com a mente imbuída de<span
lang=PT-BR style='mso-ansi-language:PT-BR'> compaixão … </span>com a mente
imbuída<span lang=PT-BR style='mso-ansi-language:PT-BR'> de alegria altruísta …
</span>com a mente imbuída<span style='mso-ansi-language:PT-BR'> <span
lang=PT-BR>de equanimidade, da mesma forma o segundo, da mesma forma o
terceiro, da mesma forma o quarto; assim acima, abaixo, em volta e em todos os
lugares e para todos, bem como para si mesmo, </span></span>ele permanece
permeando o mundo todo com a mente imbuída de<span lang=PT-BR style='mso-ansi-language:
PT-BR'> equanimidade, abundante, transcendente, imensurável, sem hostilidade e
sem má vontade.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><a name=T17></a>17. “Ele
compreende assim: ‘Existe isto, existe o inferior, existe o superior e mais
além existe a escapatória de todo esse campo de percepção.’ [</span><a
href="#N13"><span lang=PT-BR style='mso-ansi-language:PT-BR'>13</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=R13></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>18. “Ao conhecer e ver, a sua mente está livre da impureza do desejo
sensual, da impureza de ser/existir, da impureza da ignorância. Quando ela está
libertada surge o conhecimento, ‘Libertada.’ Ele compreende que ‘O nascimento
foi destruído, a vida santa foi vivida, o que deveria ser feito foi feito, não
há mais vir a ser a nenhum estado.’ Bhikkhus, esse bhikkhu é chamado de banhado
com o banho interno.” [</span><a href="#N14"><span lang=PT-BR style='mso-ansi-language:
PT-BR'>14</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=R14></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>19. Agora, naquela ocasião o brâmane Sundarika Bharadvaja estava sentado
não muito distante do Abençoado. Ele disse para o Abençoado: “Mas o Mestre
Gotama vai até o rio Bahuka para banhar?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Porque
brâmane ir até o rio Bahuka para banhar? Do que é capaz o rio Bahuka?” <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mestre
Gotama, o rio Bahuka é considerado por muitos como capaz de proporcionar a
libertação, muitos consideram que ele proporciona mérito e muitos limpam as
suas ações más no rio Bahuka.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>20. Então o
Abençoado se dirigiu ao brâmane Sundarika Bharadvaja em versos:<o:p></o:p></span></p>


<p class=Normal style='margin-left:1.0in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>&quot;Bahuka e Adhikakka,<br>

Gaya e Sundarika também,<br>

Payaga e Sarassati, <br>

e o rio Bahumati- [</span><a href="#N15"><span lang=PT-BR
style='mso-ansi-language:PT-BR'>15</span></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>]<br>

<a name=R15></a>um tolo pode neles se banhar para sempre<br>

e ainda assim não purificará as suas más ações.</p>

<p class=Normal style='margin-left:1.0in'>O que pode o Sundarika fazer acontecer?<br>

E o Payaga? E o Bahuka?<br>

Eles não são capazes de purificar quem comete o mal,<br>

um homem que tenha cometido atos brutais e cruéis.</p>

<p class=Normal style='margin-left:1.0in'>Quem possui o coração puro tem sempre <br>

a Festa da Primavera, o Dia Santo; [</span><a href="#N16"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>16</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>]<br>

<a name=R16></a>quem age de forma justa, quem possui o coração puro,<br>

conduz a sua virtude à perfeição.<br>

É aqui, brâmane, que você deveria se banhar,<br>

fazer de si mesmo um refúgio para todos os seres.</p>

<p class=Normal style='margin-left:1.0in'>E se você não disser mentiras<br>

nem causar o dano a seres vivos,<br>

nem tomar o que não for dado,<br>

com fé e livre da avareza,<br>

que necessidade terá de ir até o Gaya?<br>

Pois qualquer poço será o seu Gaya.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>21. Quando
isso foi dito o brâmane Sundarika Bharadvaja disse: “Magnífico, venerável
senhor! Magnífico, venerável senhor! O Abençoado </span>esclareceu o Dhamma de
várias formas, como se tivesse colocado em pé o que estava de cabeça para
baixo, revelasse o que estava escondido, mostrasse o caminho para alguém que
estivesse perdido ou segurasse uma lâmpada no escuro para aqueles que possuem
visão pudessem ver as formas<span lang=PT-BR style='mso-ansi-language:PT-BR'>.
Eu busco refúgio no Abençoado, no Dhamma e na Sangha dos bhikkhus. Eu receberia
a admissão na vida santa sob o Abençoado e a admissão completa.” [</span><a
href="#N17"><span lang=PT-BR style='mso-ansi-language:PT-BR'>17</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=R17></a><a name=T22></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>22. E o brâmane Sundarika Bharadvaja recebeu a
admissão na vida santa sob o Abençoado e ele recebeu a admissão completa como
bhikkhu. </span>Permanecendo só, isolado, diligente, ardente e decidido, em
pouco tempo, o venerável <span lang=PT-BR style='mso-ansi-language:PT-BR'>Bharadvaja</span>
alcançou e permaneceu no objetivo supremo da vida santa pelo qual membros de um
clã deixam a vida em família pela vida santa, tendo conhecido e realizado por
si mesmo no aqui e agora. Ele soube: “O nascimento foi destruído, a vida santa
foi vivida, o que deveria ser feito foi feito, não há mais vir a ser a nenhum
estado.” E assim o venerável <span lang=PT-BR style='mso-ansi-language:PT-BR'>Bharadvaja</span>
tornou-se mais um dos Arahants.<span lang=PT-BR style='mso-ansi-language:PT-BR'><o:p></o:p></span></p>


