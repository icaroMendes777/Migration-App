lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=N6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[6] Perfeita claridade, serenidade e confiança (<i>aveccappasada</i>) no Buda, Dhamma e Sangha é
um atributo de um nobre discípulo que tenha, pelo menos, alcançado o nível de
entrar na correnteza, cuja claridade, serenidade e confiança é perfeita porque ele viu a verdade do
Dhamma por si mesmo. As fórmulas para a recordação do Buda, Dhamma e Sangha
apresentadas aqui são explicadas em detalhe no Vsm VII. [</span><a
href="#R6"><span lang=PT-BR style='mso-ansi-language:PT-BR'>Retorna</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=N7></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[7] Esta tradução de<span style="mso-spacerun: yes">  </span><i>yatodhi</i>
segue a explicação de MA como sendo o abandono parcial das corrupções pelos
três primeiros caminhos supramundanos, contrastado com o total (<i>anodhi</i>)
abandono das corrupções pelo quarto e último caminho supramundano. [</span><a
