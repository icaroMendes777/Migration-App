<p class=Normal><a name=N8></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[8] <i>Labhati atthavedam labhati dhammavedam</i>. O Ven. Nyanaponika
interpreta: “Ele obtém entusiasmo pelo objetivo, obtém entusiasmo pelo Dhamma.”
MA explica <i>veda </i>como significando alegria e a compreensão conectada com
aquela alegria, e diz: “<i>Atthaveda </i>é a inspiração que surge naquele que
examina sua perfeita claridade, serenidade e confiança; <i>dhammaveda </i>é a inspiração que surge
naquele que examina o abandono parcial das corrupções, a causa daquela
perfeita claridade, serenidade e confiança.” [</span><a href="#R8"><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Retorna</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=N9></a><span lang=PT-BR style='mso-ansi-language:
