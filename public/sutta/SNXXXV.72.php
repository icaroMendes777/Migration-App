<p class=Normal> “Bhikkhus, se um bhikkhu não compreende como na verdade é a origem e a cessação, a gratificação, o perigo e a escapatória no caso dessas seis bases para contato, então, ele não viveu a vida santa; ele está distante deste Dhamma e Disciplina.”</p>

<p class=Normal>Quando isso foi dito, um certo bhikkhu disse para o Abençoado: “Nesse caso, venerável senhor, eu estou perdido, pois não compreendo como na verdade é a origem e a cessação, a gratificação, o perigo e a escapatória no caso dessas seis bases para contato.”</p>

<p class=Normal> “O que você pensa, bhikkhu, você considera o olho assim: ‘Isso não é meu, isso não sou eu, isso não é o meu eu?’”</p>

<p class=Normal> “Sim, venerável senhor.”</p>

<p class=Normal> “Muito bem, bhikkhu! E aqui, bhikkhu, você deve ver com clareza o olho como na verdade ele é, com correta sabedoria, assim: ‘Isso não é meu, isso não sou eu, isso não é o meu eu.’ Assim essa primeira base para o contato será abandonada para que não exista uma futura renovada existência.</p>

<p class=Normal> “Você considera o ouvido assim ...? Você considera a mente assim: ‘Isso não é meu, isso não sou eu, isso não é o meu eu?’”</p>

<p class=Normal> “Sim, venerável senhor.”</p>

<p class=Normal> “Muito bem, bhikkhu! E aqui, bhikkhu, você deve ver com clareza a mente como na verdade ela é, com correta sabedoria, assim: ‘Isso não é meu, isso não sou eu, isso não é o meu eu.’ Assim essa sexta base para o contato será abandonada para que não exista uma futura renovada existência."</p>

