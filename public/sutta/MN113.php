<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no
Parque de Anathapindika. Lá ele se dirigiu aos monges desta forma: “Bhikkhus” –
“Venerável Senhor,” eles responderam. O Abençoado disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.
“Bhikkhus, eu ensinarei sobre o caráter de um homem verdadeiro e o caráter de
um homem falso.[<a href="#N1">1</a>] <a name=R1></a>Ouçam e prestem muita
atenção àquilo que eu vou dizer.” – “Sim, venerável senhor,” os bhikkhus
responderam. O Abençoado disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3.
“Bhikkhus, qual é o caráter de um homem falso? Aqui um homem falso, que deixou
uma família aristocrática para seguir a vida santa, considera o seguinte: ‘Eu
deixei uma família aristocrática para seguir a vida santa, mas esses outros
bhikkhus não deixaram uma família aristocrática para seguir a vida santa’. Por
conseguinte, ele elogia a si mesmo e menospreza os outros devido à sua família
aristocrática. Esse é o caráter de um homem falso.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas um
homem verdadeiro considera o seguinte: ‘Não é devido à família aristocrática de
alguém que os estados de cobiça, raiva ou delusão são destruídos. Mesmo que
alguém não tenha deixado uma família aristocrática para seguir a vida santa,
ainda assim, se ele entrou no caminho que está de acordo com o Dhamma, se
entrou no caminho apropriado e se comporta de acordo com o Dhamma,<span
style='mso-spacerun:yes'>  </span>ele deve ser honrado por isso, ele deve ser
elogiado por isso.”<span style='mso-spacerun:yes'>  </span>E assim, colocando a
prática do caminho como prioritária, ele nem elogia a si mesmo e tampouco
menospreza os outros devido à sua família aristocrática. Esse é o caráter de um
homem verdadeiro.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4-6. “Além
disso, um homem falso, que deixou uma família importante para seguir a vida
santa...<span style='mso-spacerun:yes'>  </span>uma família rica ... uma
família influente, considera o seguinte: ‘Eu deixei uma família influente para
seguir a vida santa, mas esses outros bhikkhus não deixaram<span
style='mso-spacerun:yes'>  </span>uma família influente para seguir a vida
santa.’ Por conseguinte, ele elogia a si mesmo e menospreza os outros devido à
sua família influente. Esse também é o caráter de um homem falso.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas um
homem verdadeiro considera o seguinte: ‘Não é devido à família influente de
alguém que os estados de cobiça, raiva ou delusão são destruídos. Mesmo que
alguém não tenha deixado uma família influente para seguir a vida santa, ainda
assim, se ele entrou no caminho que está de acordo com o Dhamma, se entrou no
caminho apropriado e se comporta de acordo com o Dhamma, ele deve ser honrado
por isso, ele deve ser elogiado por isso.”<span style='mso-spacerun:yes'> 
</span>E assim, colocando a prática do caminho como prioritária, ele nem elogia
a si mesmo e tampouco menospreza os outros devido à sua família influente. Esse
também é o caráter de um homem verdadeiro.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. “Além
disso, um homem falso, que é bem conhecido e famoso, considera o seguinte: ‘Eu
sou bem conhecido e famoso, mas esses outros bhikkhus são desconhecidos e sem
importância.’ Por conseguinte, ele elogia a si mesmo e menospreza os outros
devido à sua fama. Esse também é o caráter de um homem falso.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas um
homem verdadeiro considera o seguinte: ‘Não é devido à fama de alguém que os estados
de cobiça, raiva ou delusão são destruídos. Mesmo que alguém não seja bem
conhecido e famoso, se ele entrou no caminho que está de acordo com o Dhamma,
se entrou no caminho apropriado e se comporta de acordo como o Dhamma, ele deve
ser honrado por isso, ele deve ser elogiado por isso.” Por conseguinte,
colocando a prática do caminho como prioritária, ele nem elogia a si mesmo e
tampouco menospreza os outros devido à sua fama. Esse também é o caráter de um
homem verdadeiro.<o:p></o:p></span></p>



<p class=Normal style='tab-stops:7.5in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>8. “Além disso, um homem falso, que obtém mantos, comida esmolada,
moradia e medicamentos, considera o seguinte: ‘Eu obtenho mantos, comida
esmolada, moradia e medicamentos; mas esses outros bhikkhus não obtêm essas
coisas.’ Por conseguinte, ele elogia a si mesmo e menospreza os outros devido a
esse ganho. Esse também é o caráter de um homem falso.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas um
homem verdadeiro considera o seguinte: ‘Não é devido ao ganho que os estados de
cobiça, raiva ou delusão são destruídos. Mesmo que alguém não ganhe nada, se
ele entrou no caminho que está de acordo com o Dhamma, entrou no caminho
apropriado e se comporta de acordo como o Dhamma, ele deve ser honrado por
isso, ele deve ser elogiado por isso.” Por conseguinte, colocando a prática do
caminho como prioritária, ele nem elogia a si mesmo e tampouco menospreza os
outros devido ao ganho. Esse também é o caráter de um homem verdadeiro.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9-20. “Além
disso, um homem falso, que é estudado ... que é um experto na Disciplina ...
que é um orador do Dhamma ... que vive nas florestas .... que veste mantos
feitos de trapos .... que se alimenta de comida esmolada ... que vive sob as
árvores .... que habita os cemitérios ... que habita a céu aberto ....
continuamente sentado .... que usa qualquer leito ...<span
style='mso-spacerun:yes'>  </span>só come uma vez ao dia, considera o seguinte:
‘Eu só como uma vez ao dia, mas esses outros bhikkhus não comem só uma vez ao
dia.’ [<a href="#N2">2</a>] <a name=R2></a>Por conseguinte, ele elogia a si
mesmo e menospreza os outros pelo fato dele só comer uma vez ao dia. Esse
também é o caráter de um homem falso.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas um
homem verdadeiro considera o seguinte: ‘Não é devido ao fato de só comer uma
vez ao dia que os estados de cobiça, raiva ou delusão são destruídos. Mesmo que
alguém não coma só uma vez ao dia, ainda assim, se ele entrou no caminho que
está de acordo com o Dhamma, entrou no caminho apropriado e se comporta de
acordo com o Dhamma, ele deve ser honrado por isso, ele deve ser elogiado por
isso.” Por conseguinte, colocando a prática do caminho como prioritária, ele
nem elogia a si mesmo e tampouco menospreza os outros devido ao fato dele só
comer uma vez ao dia. Esse também é o caráter de um homem verdadeiro.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>21. “Além
disso, um
homem falso afastado dos prazeres sensuais, afastado das qualidades não hábeis, entra e permanece no primeiro jhana, 
que é caracterizado pelo
pensamento aplicado e sustentado, com o êxtase e felicidade nascidos do
afastamento. Ele considera o seguinte: ‘Eu alcancei o primeiro jhana, mas esses
outros bhikkhus não alcançaram o primeiro jhana.’ Por conseguinte, ele elogia a
si mesmo e menospreza os outros por ele ter alcançado o primeiro jhana. Esse
também é o caráter de um homem falso.<o:p></o:p></span></p>



<p class=Normal><a name=T21></a><a name="T21_1"></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>“Mas um homem verdadeiro considera o seguinte:
‘A não identificação, ainda que com a realização do primeiro jhana, foi
declarada pelo Abençoado; pois qualquer que seja a concepção, o fato é sempre
distinto desta.’ [<a href="#N3">3</a>]<span style='mso-spacerun:yes'>  </span><a
name=R3></a>Por conseguinte, colocando a não identificação como prioritária,
ele nem elogia a si mesmo e tampouco menospreza os outros por ele ter alcançado
o primeiro jhana. Esse também é o caráter de um homem verdadeiro.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>22-24.
“Além disso, abandonando o pensamento aplicado e sustentado, um homem falso
entra e permanece no segundo jhana ... abandonando o êxtase ... ele entra e
permanece no terceiro jhana ... com o completo desaparecimento da felicidade
... ele entra e permanece no quarto jhana ...<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>25. “Além
disso, com a completa superação das percepções da forma, com o desaparecimento
das percepções do contato sensorial, sem dar atenção às percepções da
diversidade, consciente de que o ‘espaço é infinito,’ um homem falso entra e
permanece na base do espaço infinito...<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>26. “Além
disso, com a completa superação da base do espaço infinito, consciente de que a
‘consciência é infinita,’ um homem falso entra e permanece na base da
consciência infinita... <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>27. “Além
disso, com a completa superação da base da consciência infinita, consciente de
que ‘não há nada,’ um homem falso entra e permanece na base do nada...<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>28. “Além
disso, com a completa superação da base do nada, um homem falso entra e
permanece na base da nem percepção, nem não percepção. Ele considera o
seguinte: ‘Eu alcancei a base da nem percepção, nem não percepção, mas esses
outros bhikkhus não alcançaram a base da nem percepção, nem não percepção.’ Por
conseguinte, ele elogia a si mesmo e menospreza os outros por ele ter alcançado
a base da nem percepção, nem não percepção. Esse também é o caráter de um homem
falso.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas um
homem verdadeiro considera o seguinte: ‘A não identificação, ainda que com a
base da nem percepção, nem não percepção, foi declarada pelo Abençoado; pois
qualquer que seja a concepção, o fato é sempre distinto desta.’ Por
conseguinte, colocando a não identificação como prioritária, ele nem elogia a
si mesmo e tampouco menospreza os outros por ele ter alcançado a base da nem
percepção, nem não percepção. Esse também é o caráter de um homem verdadeiro. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>29. “Além
disso, com a completa superação da base da nem percepção, nem não percepção, um
homem verdadeiro entra e permanece na cessação da sensação e percepção. [<a
href="#N4">4</a>] <a name=R4></a>E as suas impurezas são destruídas ao ver com
sabedoria. Esse bhikkhu não concebe nada, ele não concebe nada em<span
style='mso-spacerun:yes'>  </span>relação a nada, ele não concebe de nenhuma
forma.” [<a href="#N5">5</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R5></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Isso foi o que disse o Abençoado. Os bhikkhus ficaram satisfeitos e
contentes com as palavras do Abençoado.<o:p></o:p></span></p>

