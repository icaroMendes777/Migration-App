<p class=Normal><a name=N3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[3] MA explica “não identificação” (<i>atammayatta</i>,
em termos literais “não consistindo disso”) como a ausência de desejo. No
entanto, o contexto sugere que o significado pode ser a ausência de presunção.
A frase “</span><span lang=PT-BR style='mso-ansi-language:PT-BR'>pois qualquer
que seja a concepção, o fato é sempre distinto desta”,<span style='mso-bidi-font-weight:
