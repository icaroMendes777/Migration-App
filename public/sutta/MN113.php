explica que isso significa que de qualquer forma que as pessoas mundanas
concebam os cinco agregados – ‘Isso é meu, isso sou eu, isso é o meu eu’ – a coisa concebida
acaba sempre sendo distinta do aspecto a ela atribuído: não é pertencente ao eu, não é “eu” ou parte do “eu.” Veja também o <a href="MN47.php#N7">MN
47, nota 7</a>.<br> Uma tradução alternativa de Ajaan Amaro para <i
style='mso-bidi-font-style:normal'>atammayatta</i>: não é feito disso, a
qualidade da consciência sem a dualidade sujeito-objeto. [<a href="#R3">Retorna</a>]<o:p></o:p></span></span></p>


