<p class=Normal> Certa ocasião o Abençoado estava numa cidade dos Mallas chamada Uruvelakappa. Então, o chefe tribal Bhadraka foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse: </p>

<p class=Normal>"Seria bom, venerável senhor, se o Abençoado me ensinasse sobre a origem e a cessação do sofrimento." </p>

<p class=Normal> “Chefe tribal, se eu lhe ensinasse a origem e a cessação do sofrimento referindo-me ao passado assim, ‘assim era no passado,’ a dúvida e a perplexidade surgiriam em você por conta disso. Se eu lhe ensinasse a origem e a cessação do sofrimento referindo-me ao futuro assim, ‘assim será no futuro,’ a dúvida e a perplexidade surgiriam em você por conta disso. Ao invés disso, chefe tribal, enquanto estou sentado exatamente aqui, e você está sentado exatamente aí, eu lhe ensinarei a origem e a cessação do sofrimento." </p>

<p class=Normal>"O que você pensa, chefe tribal? Há pessoas em Uruvelakappa que se fossem censuradas, multadas, aprisionadas, ou executadas, por conta disso surgiriam em você tristeza, lamentação, dor, angústia e desespero?" </p>

<p class=Normal>"Sim, venerável senhor, há pessoas assim." </p>

<p class=Normal>"Mas há pessoas em Uruvelakappa que se fossem censuradas, multadas, aprisionadas, ou executadas, por conta disso não surgiriam em você tristeza, lamentação, dor, angústia e desespero?" </p>

<p class=Normal>"Sim, venerável senhor, há pessoas assim." </p>

<p class=Normal>"Chefe tribal, qual é a causa e razão porque em relação a algumas pessoas em Uruvelakappa surgiriam em você tristeza, lamentação, dor, angústia e desespero se elas fossem censuradas, multadas, aprisionadas, ou executadas, enquanto que em relação a outras não surgiria a tristeza, lamentação, dor, angústia e desespero se elas fossem censuradas, multadas, aprisionadas, ou executadas?" </p>

<p class=Normal>"Aquelas pessoas em Uruvelakappa em relação às quais surgiria a tristeza, lamentação, dor, angústia e desespero se elas fossem censuradas, multadas, aprisionadas, ou executadas, são aquelas pelas quais tenho desejo e apego. Mas aquelas pessoas em Uruvelakappa em relação às quais não surgiria a tristeza, lamentação, dor, angústia e desespero se elas fossem censuradas, multadas, aprisionadas, ou executadas, são aquelas pelas quais não tenho desejo e apego." </p>

<p class=Normal>"Chefe tribal, por meio desse princípio que é visto, compreendido, realizado de imediato, entendido, aplique esse método ao passado e ao futuro assim: 'Qualquer sofrimento que tenha surgido no passado, tudo isso surgiu enraizado no desejo, com o desejo como fonte; pois o desejo é a raiz do sofrimento. 'Qualquer sofrimento que venha surgir no futuro, tudo isso surgirá enraizado no desejo, com o desejo como fonte; pois o desejo é a raiz do sofrimento.'"</p>

<p class=Normal>"É admirável, venerável senhor! É maravilhoso, venerável senhor! Quão bem isso foi dito pelo Abençoado: 'Qualquer sofrimento que surja, tudo isso surge enraizado no desejo, com o desejo como fonte; pois o desejo é a raiz do sofrimento.' Venerável senhor, eu tenho um filho chamado Ciravasi que mora numa outra residência. Eu levanto cedo e mando alguém dizendo: 'Vá e descubra como está Ciravasi.' Até que ele retorne eu fico preocupado, pensando: 'Espero que Ciravasi não esteja aflito!'"</p>

<p class=Normal>"O que você pensa, chefe tribal, se Ciravasi fosse censurado, multado, aprisionado, ou executado, surgiriam em você a tristeza, lamentação, dor, angústia e desespero?" </p>

<p class=Normal>"Venerável senhor, se Ciravasi fosse censurado, multado, aprisionado, ou executado, até mesmo a minha vida seria transtornada, então como poderiam a tristeza, lamentação, dor, angústia e desespero não surgir em mim?" </p>

<p class=Normal>"Desse modo também, chefe tribal, pode ser compreendido: 'Qualquer sofrimento que surja, tudo isso surge enraizado no desejo, com o desejo como fonte; pois o desejo é a raiz do sofrimento.'</p>

<p class=Normal>"O que você pensa, chefe tribal? Antes de ter visto ou ouvido a mãe de Ciravasi houve algum desejo, afeição, ou apego por ela?" </p>

<p class=Normal>"Não venerável senhor." </p>

<p class=Normal>"Então foi por ter visto ou ouvido a mãe de Ciravasi que esse desejo, afeição, ou apego surgiram em você?" </p>

<p class=Normal>"Sim venerável senhor." </p>

<p class=Normal>"O que você pensa, chefe tribal, se a mãe de Ciravasi fosse censurada, multada, aprisionada, ou executada, surgiriam em você a tristeza, lamentação, dor, angústia e desespero?" </p>

<p class=Normal>"Venerável senhor, se a mãe de Ciravasi fosse censurada, multada, aprisionada. ou executada, até mesmo a minha vida seria transtornada, então como poderiam a tristeza, lamentação, dor, angústia e desespero não surgir em mim?" </p>

<p class=Normal>"Desse modo também, chefe tribal, pode ser compreendido: 'Qualquer sofrimento que surja, tudo isso surge enraizado no desejo, com o desejo como fonte; pois o desejo é a raiz do sofrimento.'" </p>




