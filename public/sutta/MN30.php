<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim ouvi.
Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no Parque de
Anathapindika.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. Então, o
brâmane Pingalakoccha foi até o Abençoado e ambos se cumprimentaram. Depois que
a conversa amigável e cortês havia terminado, ele sentou a um lado e disse para
o Abençoado: <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mestre
Gotama, esses contemplativos e brâmanes, líderes de ordens, líderes de grupos,
mestres de grupos, conhecidos e famosos fundadores de seitas religiosas,
considerados como santos por muitos, como Purana Kassapa, Makkhali Gosala,
Ajita Kesakambalin, Pakudha Kaccayana, Sanjaya Belatthiputta e o Nigantha
Nataputta[<a href="#N1">1</a>] - <a name=R1></a>todos eles realizaram a verdade
como cada um deles crê ou nenhum deles a realizou, ou alguns realizaram e
outros não?” “Já basta, não importa se todos ou nenhum, ou alguns realizaram a
verdade. Vou ensinar para você o Dhamma, brâmane. Ouça e preste muita atenção
àquilo que eu vou dizer.” [<a href="#N2">2</a>]<a name=R2></a> “Sim, senhor,’ o
brâmane Pingalakoccha respondeu. O Abençoado disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3.
“Suponha, brâmane, que um homem, precisando do cerne de uma árvore, em busca do
cerne, perambulando em busca do cerne, chegasse até uma grande árvore que
possuísse um cerne. Ignorando o cerne, o alburno, a casca interna e a casca
externa ele cortasse os galhos e folhas e os levasse embora, pensando que
fossem o cerne. Então, um homem com boa visão, vendo aquilo, poderia dizer:
‘Esse bom homem não sabe o que é o cerne, o alburno, a casca interna, a casca
externa ou os galhos e folhas. E assim, precisando do cerne de uma árvore, em
busca do cerne, perambulando em busca do cerne, chegou até uma grande árvore
que possuía um cerne. Ignorando o cerne, o alburno, a casca interna e a casca
externa, ele cortou os galhos e folhas e os levou embora, pensando que fossem o
cerne. O que quer que seja que aquele bom homem tinha para fazer com o cerne, o
seu propósito não será satisfeito.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4.
“Suponham que um homem, precisando do cerne de uma árvore, em busca do cerne,
perambulando em busca do cerne, chegasse até uma grande árvore que possuísse um
cerne. Ignorando o cerne, o alburno e a casca interna, ele cortasse a casca
externa e a levasse embora, pensando que fosse o cerne. Então, um homem com boa
visão, vendo aquilo, poderia dizer: ‘Esse bom homem não sabe o que é o cerne
... ou os galhos e folhas. E assim, precisando do cerne de uma árvore ... ele
cortou a casca externa e a levou embora, pensando que fosse o cerne. O que quer
que seja que aquele bom homem tinha para fazer com o cerne, o seu propósito não
será satisfeito.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5.
“Suponham que um homem, precisando do cerne de uma árvore, em busca do cerne,
perambulando em busca do cerne, chegasse até uma grande árvore que possuísse um
cerne. Ignorando o cerne e o alburno, ele cortasse a casca interna e a levasse
embora, pensando que fosse o cerne. Então, um homem com boa visão, vendo
aquilo, poderia dizer: ‘Esse bom homem não sabe o que é o cerne ... ou os
galhos e folhas. E assim, precisando do cerne de uma árvore ... ele cortou a
casca interna e a levou embora, pensando que fosse o cerne. O que quer que seja
que aquele bom homem tinha para fazer com o cerne, o seu propósito não será
satisfeito.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6.
“Suponham que um homem, precisando do cerne de uma árvore, em busca do cerne,
perambulando em busca do cerne, chegasse até uma grande árvore que possuísse um
cerne. Ignorando o cerne, ele cortasse o alburno e o levasse embora, pensando
que fosse o cerne. Então, um homem com boa visão, vendo aquilo, poderia dizer:
‘Esse bom homem não sabe o que é o cerne ... ou os galhos e folhas. E assim,
precisando do cerne de uma árvore ... ele cortou o alburno e o levou embora,
pensando que fosse o cerne. O que quer que seja que aquele bom homem tinha para
fazer com o cerne, o seu propósito não será satisfeito.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7.
“Suponham que um homem, precisando do cerne de uma árvore, em busca do cerne,
perambulando em busca do cerne, chegasse até uma grande árvore que possuísse um
cerne, e cortando apenas o cerne, ele o levasse embora, sabendo que era o
cerne. Então, um homem com boa visão, vendo aquilo, poderia dizer: ‘Esse bom
homem sabe o que é o cerne, o alburno, a casca interna, a casca externa e os
galhos e folhas. Portanto, precisando do cerne de uma árvore, em busca do
cerne, perambulando em busca do cerne, chegou até uma grande árvore que possuía
um cerne e cortando apenas o cerne, ele o levou embora, sabendo que era o
cerne. O que quer que seja que aquele bom homem tinha para fazer com o cerne, o
seu propósito será satisfeito.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. “Da
mesma forma, brâmane, aqui, um membro de um clã com base na fé deixa a vida em
família e segue a vida santa, considerando: ‘Eu sou uma vítima do nascimento,
envelhecimento e morte, da tristeza, lamentação, dor, angústia e desespero; eu
sou uma vítima do sofrimento, uma presa do sofrimento. Com certeza, um fim a
toda essa massa de sofrimento pode ser apreendido.’ E quando ele segue a vida
santa, ele obtém ganho, honraria e fama. Ele fica satisfeito com esse ganho,
honraria e fama e a intenção dele é realizada. Por conta disso, ele elogia a si
mesmo e menospreza os outros assim: ‘Eu obtive ganho, honraria e fama, mas
esses outros bhikkhus são desconhecidos, sem importância.’ Assim, ele não
desperta o desejo de agir, ele não faz esforço para a realização daqueles
outros estados que são mais elevados e sublimes que o ganho, honraria e fama;
ele hesita e fraqueja. [<a href="#N3">3</a>] <a name=R3></a>Eu digo que essa
pessoa é igual ao homem que precisava do cerne de uma árvore, chegou até uma
grande árvore que possuía cerne e, ignorando o cerne, o alburno, a casca
interna e a casca externa, ele cortou os galhos e folhas e os levou embora,
pensando que fossem o cerne; e assim o que quer que seja que aquele bom homem
tinha para fazer com o cerne, o seu propósito não será satisfeito.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. “Aqui,
brâmane, um membro de um clã com base na fé deixa a vida em família e segue a
vida santa, considerando: ‘Eu sou uma vítima do nascimento, envelhecimento e
morte, da tristeza, lamentação, dor, angústia e desespero; eu sou uma vítima do
sofrimento, uma presa do sofrimento. Com certeza, um fim a toda essa massa de
sofrimento pode ser apreendido.’ E quando ele segue a vida santa, ele obtém
ganho, honraria e fama. Ele não fica satisfeito com esse ganho, honraria e
fama, e a intenção dele não é realizada. Por conta disso, ele não elogia a si
mesmo e não menospreza os outros. Ele desperta o desejo de agir, ele faz
esforço para a realização daqueles outros estados que são mais elevados e
sublimes que os ganho, honraria e fama; ele não hesita e não fraqueja. Ele
alcança a perfeição da virtude. Ele fica satisfeito com a perfeição da
virtude e a intenção dele é realizada. Por conta disso, ele elogia a si mesmo e
menospreza os outros assim: ‘Eu sou virtuoso, com bom caráter, mas esses outros
bhikkhus são imorais, com caráter ruim.’ Assim, ele não desperta o desejo de
agir, ele não faz esforço para a realização daqueles outros estados que são
mais elevados e sublimes do que a perfeição da virtude; ele hesita e fraqueja.
Eu digo que essa pessoa é igual ao homem que precisava do cerne de uma árvore
... ignorando o cerne, o alburno, a casca interna, ele cortou a casca externa e
a levou embora, pensando que fosse o cerne; e assim o que quer que seja que
aquele bom homem tinha para fazer com o cerne, o seu propósito não será
satisfeito.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. “Aqui,
brâmane, um membro de um clã com base na fé deixa a vida em família e segue a
vida santa, considerando: ‘Eu sou uma vítima do nascimento, envelhecimento e
morte, da tristeza, lamentação, dor, angústia e desespero; eu sou uma vítima do
sofrimento, uma presa do sofrimento. Com certeza, um fim a toda essa massa de
sofrimento pode ser apreendido.’ E quando ele segue a vida santa, ele obtém
ganho, honraria e fama. Ele não fica satisfeito com esse ganho, honraria e
fama, e a intenção dele não é realizada. Ele alcança a perfeição da virtude.
Ele fica satisfeito com a perfeição da virtude, mas a intenção dele não é
realizada. Por conta disso, ele não elogia a si mesmo e não menospreza os
outros. Ele desperta o desejo de agir, ele faz esforço para a realização
daqueles outros estados que são mais elevados e sublimes que a perfeição da
virtude; ele não hesita e não fraqueja. Ele alcança a perfeição da
concentração. Ele fica satisfeito com a perfeição da concentração e a intenção
dele é realizada. Por conta disso, ele elogia a si mesmo e menospreza os outros
assim: ‘Eu tenho concentração, minha mente está unificada, mas esses outros
bhikkhus não têm concentração, as mentes deles estão dispersas.’ Assim, ele não
desperta o desejo de agir, ele não faz esforço para a realização daqueles
outros estados que são mais elevados e sublimes do que a perfeição da
concentração; ele hesita e fraqueja. Eu digo que essa pessoa é igual ao homem
que precisava do cerne de uma árvore ... ignorando o cerne, o alburno, ele
cortou a casca interna e a levou embora, pensando que fosse o cerne; e assim o
que quer que seja que aquele bom homem tinha para fazer com o cerne, o seu
propósito não será satisfeito.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. “Aqui,
brâmane, um membro de um clã com base na fé deixa a vida em família e segue a
vida santa, considerando: ‘Eu sou uma vítima do nascimento, envelhecimento e
morte, da tristeza, lamentação, dor, angústia e desespero; eu sou uma vítima do
sofrimento, uma presa do sofrimento. Com certeza, um fim a toda essa massa de
sofrimento pode ser apreendido.’ E quando ele segue a vida santa, ele obtém ganho,
honraria e fama. Ele não fica satisfeito com esse ganho, honraria e fama, e a
intenção dele não é realizada ... Ele alcança a perfeição da virtude. Ele fica
satisfeito com a perfeição da virtude, mas a intenção dele não é realizada ...
Ele alcança a perfeição da concentração. Ele fica satisfeito com a perfeição
da concentração, mas a intenção dele não é realizada. Por conta disso, ele não
elogia a si mesmo e não menospreza os outros. Ele desperta o desejo de agir,
ele faz esforço para a realização daqueles outros estados que são mais elevados
e sublimes do que a perfeição da concentração; ele não hesita e não fraqueja.
Ele alcança o conhecimento e visão. Ele fica satisfeito com o conhecimento e
visão, e a intenção dele é realizada. Por conta disso, ele elogia a si mesmo e
menospreza os outros assim: ‘Eu vivo com o conhecimento e visão, mas esses
outros bhikkhus vivem sem conhecer nem ver.’ Assim, ele não desperta o desejo
por agir, ele não faz esforço para a realização daqueles outros estados que são
mais elevados e sublimes que o conhecimento e visão; ele hesita e fraqueja. Eu
digo que essa pessoa é igual ao homem que precisava do cerne de uma árvore ...
ignorando o cerne, ele cortou o alburno e o levou embora, pensando que fosse o
cerne; e assim, o que quer que seja que aquele bom homem tinha para fazer com o
cerne, o seu propósito não será satisfeito.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. “Aqui,
brâmane, um membro de um clã com base na fé deixa a vida em família e segue a
vida santa, considerando: ‘Eu sou uma vítima do nascimento, envelhecimento e
morte, da tristeza, lamentação, dor, angústia e desespero; eu sou uma vítima do
sofrimento, uma presa do sofrimento. Com certeza, um fim a toda essa massa de
sofrimento pode ser apreendido.’ E quando ele segue a vida santa, ele obtém ganho,
honraria e fama. Ele não fica satisfeito com esse ganho, honraria e fama, e a
intenção dele não é realizada ... Ele alcança a perfeição da virtude. Ele fica
satisfeito com a perfeição da virtude, mas a intenção dele não é realizada ...
Ele alcança a perfeição da concentração. Ele fica satisfeito com a perfeição
da concentração, mas a intenção dele não é realizada ... Ele alcança o
conhecimento e visão. Ele fica satisfeito com o conhecimento e visão, mas a
intenção dele não é realizada. Por conta disso, ele não elogia a si mesmo e não
menospreza os outros. Ele desperta o desejo de agir, ele faz esforço para a
realização daqueles outros estados que são mais elevados e sublimes do que o
conhecimento e visão; ele não hesita e não fraqueja.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas quais,
brâmane, são os estados que são mais elevados e sublimes que o conhecimento e
visão?<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. “Aqui,
brâmane, um bhikkhu afastado dos prazeres sensuais, afastado das qualidades não hábeis, entra e permanece no primeiro jhana, que é caracterizado pelo pensamento aplicado e sustentado, com o êxtase e felicidade nascidos do afastamento. Esse é um
estado mais elevado e sublime do que o conhecimento e visão. [<a href="#N4">4</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>14. “Outra vez, abandonando o pensamento aplicado e sustentado, um bhikkhu entra e permanece no segundo jhana, que é caracterizado pela segurança interna e perfeita unicidade da mente, sem o pensamento aplicado e sustentado, com o êxtase e felicidade nascidos da concentração. Esse também é um estado mais
elevado e sublime do que o conhecimento e visão.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. “Outra
vez, abandonando o êxtase, um bhikkhu entra e permanece no terceiro jhana que é caracterizado pela felicidade sem o êxtase, acompanhada pela atenção plena, plena consciência e equanimidade, acerca do qual os nobres declaram: ‘Ele permanece numa estada feliz, equânime e plenamente atento.’ Esse
também é um estado mais elevado e sublime do que o conhecimento e visão. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16. “Outra
vez, com o completo desaparecimento da felicidade, um bhikkhu entra e permanece no quarto jhana, que possui nem felicidade nem sofrimento, com a atenção plena e a equanimidade purificadas. Esse também é um estado mais elevado e sublime do que o
conhecimento e visão.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17. “Outra
vez, com a completa superação das percepções da forma, com o desaparecimento
das percepções do contato sensorial, sem dar atenção às percepções da
diversidade, consciente de que o ‘espaço é infinito,’ um bhikkhu entra e
permanece na base do espaço infinito. Esse também é um estado mais elevado e
sublime do que o conhecimento e visão.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18. “Outra
vez, com a completa superação da base do espaço infinito, consciente de que a
‘consciência é infinita,’ um bhikkhu entra e permanece na base da consciência
infinita. Esse também é um estado mais elevado e sublime do que o conhecimento
e visão.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>19. “Outra
vez, com a completa superação da base da consciência infinita, consciente de
que ‘não há nada,’ um bhikkhu entra e permanece na base do nada. Esse também é
um estado mais elevado e sublime do que o conhecimento e visão.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>20. “Outra
vez, com a completa superação da base do nada, um bhikkhu entra e permanece na
base da nem percepção, nem não percepção. Esse também é um estado mais elevado
e sublime do que o conhecimento e visão.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>21. “Outra
vez, com a completa superação da base da nem percepção, nem não percepção, um
bhikkhu entra e permanece na cessação da percepção e sensação. E as suas
impurezas são destruídas ao ver com sabedoria. Esse também é um estado mais
elevado e sublime do que o conhecimento e visão. Esses são os estados mais
elevados e sublimes do que o conhecimento e visão.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>22. “Eu
digo que essa pessoa, brâmane, é como um homem que, precisando do cerne de uma
árvore, em busca do cerne, perambulando em busca do cerne, chega até uma grande
árvore que possui um cerne, e cortando apenas o cerne, ele o leva embora,
sabendo que é o cerne; e assim o que quer que seja que aquele bom homem tinha
para fazer com o cerne, o seu propósito será satisfeito.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>23.
“Portanto, brâmane, esta vida santa não tem o ganho, honraria e fama como seu
benefício, ou a perfeição da virtude como seu benefício, ou a perfeição da
concentração como seu benefício, ou o conhecimento e visão como seu benefício.
Mas é a<span style='mso-spacerun:yes'>  </span>libertação inabalável da mente
que é o objetivo desta vida santa, o seu cerne e o seu fim.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>24. Quando
isso foi dito, o brâmane Pingalakoccha disse para o Abençoado: “Magnífico ,
Mestre Gotama! Magnífico, Mestre Gotama! Mestre Gotama esclareceu o Dhamma de
várias formas, como se tivesse colocado em pé o que estava de cabeça para
baixo, revelasse o que estava escondido, mostrasse o caminho para alguém que
estivesse perdido ou segurasse uma lâmpada no escuro para aqueles que possuem
visão pudessem ver as formas. Eu busco refúgio no Mestre Gotama, no Dhamma e na
Sangha dos bhikkhus. Que o Mestre Gotama me aceite como discípulo leigo que
buscou refúgio para o resto da vida.<o:p></o:p></span></p>

