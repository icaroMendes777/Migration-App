<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em Savathi.
Então o venerável Kaccayana Gotta se aproximou do Abençoado e depois de
cumprimentá-lo sentou a um lado e disse para o Abençoado: “Venerável senhor, é
dito ‘Entendimento correto, entendimento correto.’ De que forma existe
entendimento correto?”<o:p></o:p></span></p>



<p class=Normal><a name=T2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Kaccayana, em geral este mundo depende de uma dualidade, a noção da
existência e a noção da não existência. Mas para aquele que vê com correta
sabedoria a origem do mundo, tal como na verdade ela ocorre, a noção da ‘não
existência’ com relação ao mundo não lhe ocorrerá. Aquele que vê com correta
sabedoria a cessação do mundo, tal como<span style='mso-spacerun:yes'> 
</span>na verdade ela ocorre, a noção da ‘existência’ com relação ao mundo não
lhe ocorrerá.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Kaccayana,
em geral este mundo é aprisionado por adesões, apegos e preconceitos. Mas uma
pessoa como essa [com entendimento correto] não se envolve ou se apega através
dessas adesões, apegos, fixações mentais, inclinações ou obsessões; e ela não
toma uma determinação com relação ao ‘meu eu’; ela não tem dúvida ou
perplexidade que aquilo que surge é apenas o sofrimento surgindo, aquilo que
cessa é apenas o sofrimento cessando. O conhecimento dela com relação a isso
não depende dos outros. É com referência a isso, Kaccayana, que existe o
entendimento correto.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’Tudo
existe’: Kaccayana, esse é um extremo. ‘Tudo não existe’: esse é o outro
extremo. Evitando esses dois extremos, o <a name=PVW>Tathagata</a> ensina o
Dhamma pelo meio: Da ignorância como condição, as formações volitivas [surgem]. Das
formações volitivas como condição, a consciência. Da consciência como condição, a
mentalidade-materialidade (nome e forma). Da mentalidade-materialidade (nome e forma) como condição, as seis
bases dos sentidos. Das seis bases dos sentidos como condição, o contato. Do
contato como condição, a sensação. Da sensação como condição, o desejo. Do
desejo como condição, o apego. Do apego como condição, o ser/existir. Do
ser/existir como condição, o nascimento. Do nascimento como condição, então o
envelhecimento e morte, tristeza, lamentação, dor, angústia e desespero surgem.
Essa é a origem de toda essa massa de sofrimento.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Agora, do
desaparecimento e cessação sem deixar vestígios dessa mesma ignorância cessam
as formações volitivas. Da cessação das formações volitivas cessa a consciência. Da cessação da
consciência cessa a mentalidade-materialidade (nome e forma). Da cessação da
mentalidade-materialidade (nome e forma) cessam as seis bases dos sentidos. Da cessação das
seis bases dos sentidos cessa o contato. Da cessação do contato cessa a
sensação. Da cessação da sensação cessa o desejo. Da cessação do desejo cessa o
apego. Da cessação do apego cessa o ser/existir. Da cessação do ser/existir
cessa o nascimento. Da cessação do nascimento, então o envelhecimento e morte,
tristeza, lamentação, dor, angústia e desespero, tudo cessa. Essa é a cessação
de toda essa massa de sofrimento.”<o:p></o:p></span></p>

