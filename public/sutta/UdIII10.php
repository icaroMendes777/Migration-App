<p class=Normal><a name="N2"> </a> [2] Os cinco agregados, (forma, sensação, percepção, consciência, formações) são tratados como doença visto que são essencialmente insatisfatórios e a base do sofrimento. Mas devido à ignorância, entendimento incorreto e o desejo sensual, as pessoas comuns se equivocam ao considerá-los como permanentes, constantes, prazerosos e como um “eu” ou “meu”. No entanto, não importa como elas os concebam – por serem impermanentes, insatisfatórios e não-eu – a realidade será distinta da concepção deludida. [<a href="#R2">Retorna</a>] </p>
 
<p class=Normal><a name="N3"> </a> [3] Estas duas sentenças podem ser compreendidas como uma referência respectivamente aos adeptos da eternidade e os adeptos da aniquilação ou materialistas, os primeiros afirmando a permanência de um eu e os últimos aceitando um eu temporário destinado à aniquilação. O “caminho do meio” da origem dependente e da condicionalidade evita esses dois extremos. [<a href="#R3">Retorna</a>] </p>
 
