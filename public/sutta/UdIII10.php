<p class=Normal>Assim ouvi. Certa ocasião o Abençoado estava em Uruvela às margens do rio Neranjara ao pé de uma figueira-dos-pagodes, pouco tempo depois de ter alcançado a perfeita iluminação – ele ficou sentado em meditação sob a sombra dessa árvore por sete dias sem parar, sensível à felicidade da libertação. Ao final de sete dias, após emergir daquela concentração, ele examinou o mundo com o olho de um Buda. Ao fazer isso, ele viu seres ardendo com muitas febres e em chamas com o fogo da cobiça, da raiva e da delusão. [<a href="#N1">1</a>] <a name="R1"> </a>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p>
 
<p class=Normal style='margin-left:.75in'> O mundo está em chamas.<br>
Afligido pelo contato, <br>
nomeia a doença - “eu”. <br>
Pois qualquer que seja a concepção,<br> 
o fato é sempre distinto desta. [<a href="#N2">2</a>] </p>
 
<p class=Normal style='margin-left:.75in'><a name="R2"> </a> Tornando-se distinto, <br>
o mundo é <br>
&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp;suportado pelo ser/existir, <br> 
&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp;atormentado pelo ser/existir,<br> 
mas no entanto se delicia <br>
&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp;com esse mesmo ser/existir. </p>
 
<p class=Normal style='margin-left:.75in'> Onde há deleite, <br> 
&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp;há medo. <br>
Aquilo que amedronta, <br>
&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp;causa sofrimento. <br>
Esta vida santa é vivida<br>
para o abandono do ser/existir. </p> 
<p class=Normal> “Quaisquer contemplativos ou brâmanes que tenham dito que a libertação do ser/existir ocorre através de algum outro tipo de ser/existir, nenhum deles, eu digo, estão livres do ser/existir. E quaisquer contemplativos ou brâmanes que tenham dito que a libertação do ser/existir ocorre através de algum tipo de não ser/existir, nenhum deles, eu digo, estão livres do ser/existir. [<a href="#N3">3</a>] <a name="R3"> </a>Este sofrimento surge na dependência das aquisições. Com o fim de todo apego, nenhum sofrimento é produzido.[ <a href="#N4">4</a>] </p>
<p class=Normal style='margin-left:.75in'><a name="R4"> </a> Olhem para o mundo,<br>
os seres atormentados pela ignorância <br>
não estão libertos<br>
do deleite por aquilo que é/existe. <br> 
Todos os tipos de ser/existir, <br>
&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp;em qualquer lugar, <br>
&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp;de qualquer modo, <br> 
são impermanentes, <br>
sujeitos ao sofrimento, cuja natureza é a mudança. </p>
 
<p class=Normal style='margin-left:.75in'> Vendo isso como na verdade é, <br>
com correta sabedoria, <br>
o desejo por ser/existir é abandonado, <br>
sem no entanto se deliciar com o não ser/existir. <br>
Nibbana é o completo desapego e cessação, <br>
realizado com a completa destruição do desejo. </p> 
 
<p class=Normal style='margin-left:.75in'> Para um bhikkhu cujos desejos foram extintos<br>
através do desapego, não há mais ser/existir. <br>
Mara foi derrotado, a batalha vencida: <br>
tendo superado todos os tipos de ser/existir, <br>
&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp; <a name="R5"> </a>ele é Assim. [<a href="#N5">5</a>] </p>

