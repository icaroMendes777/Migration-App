<p class=Normal> (118) "Bhikkhus, há dois desejos difíceis de serem abandonados. Quais dois? O desejo pelo ganho e o desejo pela vida. Esses dois desejos são difíceis de serem abandonados." </p>

<p class=Normal>Nota: ganho é <i>labha</i> em pali que também pode significar aquisições, posses. </p>

<p class=Normal> (119) "Bhikkhus, há esses dois tipos de pessoas que são raras no mundo. Quais duas? Aquela que toma a iniciativa em ajudar os outros e aquela que é grata e agradecida. Esses dois tipos de pessoas são raras no mundo." </p>

<p class=Normal> (120) "Bhikkhus, há esses dois tipos de pessoas que são raras no mundo. Quais duas? Aquela que está satisfeita e aquela que proporciona satisfação. Esses dois tipos de pessoas são raras no mundo." </p>

<p class=Normal> (121) "Bhikkhus, esses dois tipos de pessoas são difíceis de satisfazer. Quais duas? Aquela que acumula aquilo que ganha e aquela que desperdiça aquilo que ganha. Esses dois tipos de pessoas são difíceis de satisfazer." </p>

<p class=Normal> (122) "Bhikkhus, esses dois tipos de pessoas são fáceis de satisfazer. Quais duas? Aquela que não acumula aquilo que ganha e aquela que não desperdiça aquilo que ganha. Esses dois tipos de pessoas são fáceis de satisfazer." </p>

<p class=Normal> (123) "Bhikkhus, há duas condições para o surgimento da cobiça. Quais duas? O sinal do atrativo e a atenção sem sabedoria. Essas são as duas condições para o surgimento da cobiça." </p>

<p class=Normal>Nota: sinal do atrativo: <i>subhanimitta</i>. Atenção sem sabedoria: <i>ayoniso manasikara</i></p>

<p class=Normal> (124) "Bhikkhus, há duas condições para o surgimento da raiva. Quais duas? O sinal do repulsivo e a atenção sem sabedoria. Essas são as duas condições para o surgimento da raiva." </p>

<p class=Normal> (125) "Bhikkhus, há duas condições para o surgimento do entendimento incorreto. Quais duas? As palavras ditas por alguém e a atenção sem sabedoria. Essas são as duas condições para o surgimento do entendimento incorreto." </p>

<p class=Normal> (126) "Bhikkhus, há duas condições para o surgimento do entendimento correto. Quais duas? As palavras ditas por alguém e a atenção com sabedoria. Essas são as duas condições para o surgimento do entendimento correto." </p>

<p class=Normal> (127) "Bhikkhus, há dois tipos de ofensas. Quais duas? Uma ofensa grave e uma ofensa leve. Esses são os dois tipos de ofensas." </p>

<p class=Normal> (128) "Bhikkhus, há dois tipos de ofensas. Quais duas? Uma ofensa vulgar e uma ofensa não vulgar. Esses são os dois tipos de ofensas." </p>

<p class=Normal> (128) "Bhikkhus, há dois tipos de ofensas. Quais duas? Uma ofensa remediável e uma ofensa irremediável. Esses são os dois tipos de ofensas." </p>


