<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em certa
ocasião o Abençoado estava em <span class=SpellE>Savatthi</span>, no Bosque de
Jeta, no Parque de <span class=SpellE>Anathapindika</span>. Então o rei <span
class=SpellE>Pasenadi</span> de Kosala foi até o Abençoado e depois de
cumprimentá-lo sentou a um lado. Agora, naquela ocasião a rainha <span
class=SpellE>Mallika</span> havia morrido. Então <span class=GramE>um certo</span>
homem foi até o rei e sussurrou no seu ouvido: &quot;Majestade, a rainha <span
class=SpellE>Mallika</span> morreu.&quot; Quando isto foi dito, o rei <span
class=SpellE>Pasenadi</span> de Kosala ficou triste e deprimido, com os ombros
caídos, a cabeça baixa, sombrio, sem palavras. <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o
Abençoado viu o rei ali sentado triste e <span class=GramE>deprimido …</span>
sem palavras, e lhe disse, &quot;Há essas cinco coisas, grande rei, que não
podem ser obtidas por um contemplativo, um brâmane, um deva, Mara, Brahma, ou
qualquer pessoa no mundo. Quais cinco? <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;'Possa
aquilo que está sujeito ao envelhecimento não <span class=GramE>envelhecer.</span>'
Isto é algo que não pode ser obtido por um contemplativo, um brâmane, um deva,
Mara, Brahma, ou qualquer pessoa no mundo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;'Possa
aquilo que está sujeito à enfermidade não <span class=GramE>enfermar.</span>' Isto
é algo que não pode ser obtido por um contemplativo, um brâmane, um deva, Mara,
Brahma, ou qualquer pessoa no mundo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;'Possa
aquilo que está sujeito à morte não <span class=GramE>morrer.</span>' Isto é
algo que não pode ser obtido por um contemplativo, um brâmane, um deva, Mara,
Brahma, ou qualquer pessoa no mundo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;'Possa
aquilo que está sujeito à destruição não <span class=GramE>ser destruído.</span>' Isto
é algo que não pode ser obtido por um contemplativo, um brâmane, um deva, Mara,
Brahma, ou qualquer pessoa no mundo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;'Possa
aquilo que está sujeito à perda não ser perdido.</span>'
Isto é algo que não pode ser obtido por um contemplativo, um brâmane, um deva,
Mara, Brahma, ou qualquer pessoa no mundo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Grande
Rei, para uma pessoa comum sem instrução, aquilo que está sujeito ao
envelhecimento, envelhece, aquilo que está sujeito à
enfermidade, enferma, aquilo que está sujeito à
morte, morre, aquilo que está sujeito à destruição, é destruído, aquilo que
está sujeito à perda, é perdido. E quando essas coisas acontecem, ela não reflete: ‘Não acontece somente comigo que aquilo que está sujeito
ao envelhecimento, envelhece ... aquilo que está sujeito à perda, é perdido, mas sempre que há seres, indo e vindo, falecendo e renascendo, para
todos esses seres aquilo que está sujeito ao envelhecimento, envelhece ...
aquilo que está sujeito à perda, é perdido. E se, com o envelhecimento
daquilo que está sujeito ao envelhecimento .. com a perda daquilo que está
sujeito a ser perdido, eu ficasse triste, angustiada, lamentasse, batesse no
peito e ficasse confusa, a comida não me cairia bem, meu corpo ficaria
emaciado, meu trabalho não seria feito,<span style='mso-spacerun:yes'> 
</span>meus inimigos ficariam contentes e meus amigos tristes.’ Então, com o
envelhecimento daquilo que está sujeito ao envelhecimento, com a enfermidade daquilo que está sujeito à
enfermidade, com a morte daquilo
que está sujeito a morrer, com a destruição daquilo que está sujeito a ser destruído, com a perda daquilo que está sujeito a ser perdido, ela fica
triste, angustiada, lamenta, bate no peito e fica confusa. A isto se chama uma
pessoa comum sem instrução penetrada pela flecha envenenada da tristeza,
atormentando a si mesma.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Grande
Rei, para um nobre discípulo bem instruído, aquilo que está sujeito ao
envelhecimento, envelhece, aquilo que está sujeito à
enfermidade, enferma, aquilo que está sujeito à
morte, morre, aquilo que está sujeito à destruição, é destruído, aquilo que
está sujeito à perda, é perdido. E quando essas coisas acontecem, ele reflete: ‘Não acontece somente comigo que aquilo que está sujeito ao
envelhecimento, envelhece ... aquilo que está sujeito à perda, é perdido, mas sempre que há seres, indo e vindo, falecendo e renascendo, para
todos esses seres aquilo que está sujeito ao envelhecimento, envelhece ...
aquilo que está sujeito à perda, é perdido. E se, com o envelhecimento
daquilo que está sujeito ao envelhecimento .. com a perda daquilo que está
sujeito a ser perdido, eu ficasse triste, angustiado, lamentasse, batesse no
peito e ficasse confuso, a comida não me cairia bem, meu corpo ficaria
emaciado, meu trabalho não seria feito, meus inimigos ficariam contentes e meus
amigos tristes.’ Então, com o envelhecimento daquilo que está sujeito ao
envelhecimento, com a enfermidade daquilo que está sujeito à
enfermidade, com a morte daquilo que está sujeito a morrer, com a destruição
daquilo que está sujeito a ser destruído, com a perda daquilo que está sujeito
a ser perdido, ele não fica triste, angustiado, não lamenta, bate no peito e
fica confuso. A isto se chama um nobre discípulo bem instruído que arrancou a
flecha envenenada da tristeza através da qual a pessoa comum atormenta a si
mesma. Livre da tristeza, livre da flecha, o nobre discípulo está libertado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Grande
Rei, essas são as cinco coisas que não podem ser obtidas por um contemplativo,
um brâmane, um deva, Mara, Brahma, ou qualquer pessoa no mundo.&quot; <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR>Não se entristeça, nem <span
class=GramE>lamente.</span><br>
Aqui, que benefício poderá ser obtido? – nenhum de <span class=GramE>fato,</span><br>
e os inimigos se alegram ao ver essa tristeza e dor.<br>
Mas quando os infortúnios não abalam o sábio -<br>
aquele que sabe bem como buscar o que é benéfico,<br>
então devido a isso os seus inimigos é que sofrem<br>
ao ver o seu rosto inalterado, sem tensão.<br>
Onde e de que modo um objetivo possa ser alcançado<br>
através de <span class=SpellE>eulogias</span>, cantos, boas palavras,<br>
donativos e costumes familiares,<br>
siga-os com diligência ali e desse modo.<br>
Porém se você discernir que o seu próprio objetivo<br>
ou aquele de outros, não é alcançado desse modo,<br>
submeta-se [à natureza das coisas]<br>
sem tristeza, com o pensamento:<br>
'Que tarefa importante estou fazendo agora?' </span></p>

