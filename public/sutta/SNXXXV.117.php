<p class=Normal> “Bhikkhus, antes da minha iluminação, quando eu ainda era um Bodisatva não iluminado, o pensamento me ocorreu: 'Minha mente com freqüência vai na direção desses cinco elementos do prazer sensual que deixaram a sua impressão no coração mas que se foram, cessaram e mudaram, ou na direção daqueles que estão presentes ou ligeiramente na direção daqueles no futuro.' Então eu pensei: 'Determinado pelo meu próprio bem-estar, eu deveria praticar diligente, com atenção plena, protegendo a mente desses cinco elementos do prazer sensual que já deixaram a sua impressão no coração mas que se foram, cessaram e mudaram.'</p>

<p class=Normal>"Portanto, bhikkhus, no seu caso também, as suas mentes podem com freqüência ir na direção desses cinco elementos do prazer sensual que deixaram a sua impressão no coração mas que se foram, cessaram e mudaram, ou na direção daqueles que estão presentes ou ligeiramente na direção daqueles no futuro.' Assim, bhikkhus, determinados pelo seu próprio bem-estar, vocês deveriam praticar diligentes, com atenção plena, protegendo a mente desses cinco elementos do prazer sensual que já deixaram a sua impressão no coração mas que se foram, cessaram e mudaram. </p>

<p class=Normal>"Portanto, bhikkhus, essa base deve ser compreendida, na qual o olho cessa e a percepção das formas cessa. Essa base deve ser compreendida, na qual o ouvido cessa e a percepção dos sons cessa ... Essa base deve ser compreendida, na qual a mente cessa e a percepção dos objetos mentais cessa. Essa base deve ser compreendida." </p>

<p class=Normal>Tendo dito isso, o Abençoado se levantou do seu assento e foi para a sua moradia. Então, pouco tempo depois do Abençoado haver partido, os bhikkhus consideraram: “Agora, amigos, o Abençoado levantou-se do seu assento e foi para a sua moradia depois de expor um resumo sem explicar o seu significado em detalhe. Agora, quem irá explicar o significado em detalhe?” Então eles consideraram: “O venerável Ananda é elogiado pelo Mestre e estimado pelos seus sábios companheiros da vida santa. Ele é capaz de explicar o significado em detalhe. E se fôssemos até ele e pedíssemos a explicação do significado disso.” </p>

<p class=Normal>Então, os bhikkhus foram até o venerável Ananda e o cumprimentaram. Quando a conversa cortês e amigável havia terminado, eles sentaram a um lado e contaram o que havia acontecido, adicionando: "Que o venerável Ananda nos explique isso." </p>

<p class=Normal> [O Venerável Ananda respondeu:] “Amigos, é como se um homem que precisa de madeira, procurasse madeira, perambulasse em busca de madeira, pensasse que a madeira deveria ser procurada entre os galhos e as folhas de uma grande árvore que possui madeira, depois de haver passado por cima da sua raiz e tronco. O mesmo ocorre com vocês, veneráveis senhores, que pensam que eu deva ser perguntado sobre o significado disso, depois de terem passado pelo Abençoado, estando cara a cara com o Mestre. Pois, conhecer, o Abençoado conhece; ver, ele vê; ele é visão, ele é conhecimento, ele é o Dhamma, ele é o sagrado; ele é o que diz, o que proclama, o que elucida o significado, o que provê o imortal, o senhor do Dhamma, o Tathagata. Aquele foi o momento em que vocês deveriam ter perguntado ao Abençoado o significado. O que ele dissesse vocês deveriam se lembrar.” </p>

<p class=Normal> “Certamente, amigo Ananda, conhecer, o Abençoado conhece; ver, ele vê; ele é visão … o Tathagata. Aquele foi o momento em que deveríamos ter perguntado ao Abençoado o significado. O que ele nos dissesse nós deveríamos nos lembrar. No entanto, o venerável Ananda é elogiado pelo Mestre e estimado pelos seus sábios companheiros da vida santa. O venerável Ananda é capaz de expor o significado, em detalhe, desse resumo dito pelo Abençoado. Que o venerável Ananda possa expor isso, sem que isso seja um problema.” </p>

<p class=Normal> “Então, amigos, ouçam e prestem muita atenção àquilo que eu vou dizer." - "Sim, amigo," os bhikkhus responderam. O venerável Ananda disse o seguinte: </p>

<p class=Normal> “Amigos, quando o Abençoado se levantou do seu assento e foi para a sua moradia depois de expor um resumo sem explicar o seu significado em detalhe, isto é: 'Portanto, bhikkhus, essa base deve ser compreendida, na qual o olho cessa e a percepção das formas cessa. Essa base deve ser compreendida, na qual o ouvido cessa e a percepção dos sons cessa ... Essa base deve ser compreendida, na qual a mente cessa e a percepção dos objetos mentais cessa. Essa base deve ser compreendida,' eu entendo que o significado em detalhe é o seguinte: Isso foi dito pelo Abençoado, amigos, com referências à cessação das seis bases. </p>

<p class=Normal> “Amigos, quando o Abençoado se levantou do seu assento e foi para a sua moradia depois de expor um resumo sem explicar o seu significado em detalhe, ... é assim como eu entendo o significado em detalhe. Agora, amigos, se vocês quiserem, podem ir até o Abençoado perguntar-lhe qual o significado disso. Exatamente aquilo que o Abençoado explicar é o que vocês deverão se lembrar.” </p>

<p class=Normal>Então, os bhikkhus, tendo se alegrado e se deliciado com as palavras do venerável Ananda, levantaram-se dos seus assentos e foram até o Abençoado. Após homenageá-lo, eles sentaram a um lado e relataram ao Abençoado aquilo que havia ocorrido depois que ele havia partido, adicionando o seguinte: “Então, venerável senhor, fomos até o venerável Ananda e perguntamos a ele sobre o significado. O venerável Ananda nos explicou o significado com estes termos, afirmações e frases.” </p>

<p class=Normal> “Ananda é sábio, bhikkhus, Ananda possui muita sabedoria. Se vocês me tivessem perguntado o significado, eu teria explicado do mesmo modo que Ananda explicou. Esse é o significado e é assim como vocês deverão se lembrar.” </p>

