<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim ouvi.
Em certa ocasião, o Venerável Anuruddha estava<span style='mso-spacerun:yes'> 
</span>em Savatthi no Bosque de Jeta, no Parque de Anathapindika. Então, enquanto
o Venerável Anuruddha estava só, em isolamento, este pensamento surgiu na sua
mente: “Aqueles que negligenciaram estes quatro fundamentos da atenção plena
negligenciaram o nobre caminho que conduz à completa destruição do sofrimento.
Aqueles que adotaram esses quatro fundamentos da atenção plena adotaram o nobre
caminho que conduz à completa destruição do sofrimento.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, o
Venerável Mahamoggallana, tendo percebido com a sua mente o
pensamento na mente do Venerável Anuruddha, com a mesma rapidez com que um
homem forte pode estender o seu braço flexionado ou flexionar o seu braço
estendido, apareceu na frente do Venerável Anuruddha e lhe disse:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Até que
ponto, amigo Anuruddha, esses quatro fundamentos da atenção plena foram
adotados por um bhikkhu?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
amigo, um bhikkhu permanece contemplando a origem do corpo, ou ele permanece
contemplando a cessação do corpo ou ele permanece contemplando tanto a origem
como a cessação do corpo, ardente, plenamente consciente e com atenção plena,
tendo colocado de lado a cobiça e o desprazer pelo mundo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele
permanece contemplando a origem do corpo externamente, ou ele permanece
contemplando a cessação do corpo externamente, ou ele permanece contemplando
tanto a origem como a cessação do corpo externamente, ardente, plenamente
consciente e com atenção plena, tendo colocado de lado a cobiça e o desprazer
pelo mundo.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele permanece
contemplando a origem do corpo internamente e externamente, ou ele permanece
contemplando a cessação do corpo internamente e externamente, ou ele permanece
contemplando tanto a origem como a cessação do corpo internamente e
externamente, ardente, plenamente consciente e com atenção plena, tendo
colocado de lado a cobiça e o desprazer pelo mundo<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se ele
desejar: [<a href="#N1">1</a>] <a name=R1></a>‘Que eu permaneça percebendo o
repulsivo no não repulsivo,’ ele permanece percebendo o repulsivo naquilo. Se
ele desejar: ‘Que eu permaneça percebendo o não repulsivo no repulsivo,’ ele
permanece percebendo o não repulsivo naquilo. Se ele desejar: ‘Que eu permaneça
percebendo o repulsivo no não repulsivo e no repulsivo,’ ele permanece
percebendo o repulsivo naquilo. Se ele desejar: ‘Que eu permaneça percebendo o
não repulsivo no repulsivo e no não repulsivo,’ ele permanece percebendo o não
repulsivo naquilo. Se ele desejar: ‘Evitando ambos, o não repulsivo e o
repulsivo, que eu permaneça equânime, com atenção plena e plena consciência,’
então, ele permanece assim equânime, com atenção plena e plena consciência.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele
permanece contemplando a origem ... a cessação ... a origem e a cessação das
sensações internamente ... das sensações externamente ... das sensações
internamente e externamente, ardente, plenamente consciente e com atenção
plena, tendo colocado de lado a cobiça e o desprazer pelo mundo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se ele
desejar: ‘Que eu permaneça percebendo o repulsivo no não repulsivo,’ ele
permanece percebendo o repulsivo naquilo ... Se ele desejar: ‘Evitando ambos, o
não repulsivo e o repulsivo, que eu permaneça equânime, com atenção plena e
plena consciência,’ então ele permanece assim equânime, com atenção plena e
plena consciência.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele
permanece contemplando a origem ... a cessação ... a origem e a cessação da
mente externamente ... da mente internamente e externamente, ardente,
plenamente consciente e com atenção plena, tendo colocado de lado a cobiça e o
desprazer pelo mundo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se ele
desejar: ‘Que eu permaneça percebendo o repulsivo no não repulsivo,’ ele
permanece percebendo o repulsivo naquilo ... Se ele desejar: ‘Evitando ambos, o
não repulsivo e o repulsivo, que eu permaneça equânime, com atenção plena e
plena consciência,’ então, ele permanece assim equânime, com atenção plena e
plena consciência.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele
permanece contemplando a origem ... a cessação ... a origem e a cessação dos
objetos mentais externamente ... dos objetos mentais internamente e
externamente, ardente, plenamente consciente e com atenção plena, tendo
colocado de lado a cobiça e o desprazer pelo mundo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se ele
desejar: ‘Que eu permaneça percebendo o repulsivo no não repulsivo,’ ele
permanece percebendo o repulsivo naquilo ... Se ele desejar: ‘Evitando ambos, o
não repulsivo e o repulsivo, que eu permaneça equânime, com atenção plena e
plena consciência,’ então, ele permanece assim equânime, com atenção plena e
plena consciência.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“É desse
modo, amigo, que os quatro fundamentos da atenção plena foram adotados por um
bhikkhu.”<o:p></o:p></span></p>

