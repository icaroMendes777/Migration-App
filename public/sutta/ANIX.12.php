<p class=Normal> Certa ocasião o Abençoado estava em Savathi no Bosque de Jeta, no Parque de Anathapindika. Então ao amanhecer, o venerável Sariputta se vestiu e tomando a tigela e o manto externo, foi para Savathi para esmolar alimentos. Então ele pensou: ainda é muito cedo para esmolar alimentos em Savathi. E se eu fosse até o parque dos errantes das outras seitas.” </p>

<p class=Normal>Então o venerável Sariputta foi até onde os errantes de outras seitas estavam e eles se cumprimentaram. Quando a conversa amigável e cortês havia terminado, ele sentou a um lado. Agora naquela ocasião aqueles errantes haviam se reunido e estavam sentados juntos quando surgiu este assunto: <a name=R1></a>"Amigos, qualquer um que faleça com combustível restante [<a href="#N1">1</a>] não está livre do inferno, do mundo animal ou do mundo dos fantasmas famintos; ele não está livre do plano de miséria, de um destino ruim, dos mundos inferiores. </p>

<p class=Normal>Então, o venerável Sariputta nem aprovou ou desaprovou as palavras dos errantes, mas levantou do seu assento e partiu, [pensando]: “Devo aprender o significado dessa afirmação na presença do Abençoado.” </p>

<p class=Normal>Então, depois de haver esmolado em Savathi e de haver retornado, após a refeição, ele foi até o Abençoado e depois de cumprimentá-lo sentou a um lado. [Em seguida ele relatou o que havia acontecido, concluindo:] "Levantei do meu assento e parti, [pensando]: 'Devo aprender o significado dessa afirmação na presença do Abençoado.'"</p>

<p class=Normal>"Quem, Sariputta, são esses errantes de outras seitas tolos e incompetentes e quem são aqueles que conhecem alguém com combustível restante como  'alguém com combustível restante' e alguém sem combustível restante como 'alguém sem combustível restante'? </p>

<p class=Normal>"Essas nove pessoas, Sariputta, falecendo com combustível restante, estão livres do inferno, do mundo animal ou do mundo dos fantasmas famintos; elas estão livres do plano de miséria, de um destino ruim, dos mundos inferiores. Quais nove? </p>

<p class=Normal> (1) "Aqui, Sariputta, uma certa pessoa satisfaz o comportamento virtuoso e a concentração mas cultiva a sabedoria de um modo moderado. Com a completa destruição dos cinco primeiros grilhões, essa pessoa realiza nibbana no intervalo. Essa é a primeira pessoa que falece com combustível restante que está livre do inferno, do mundo animal ou do mundo dos fantasmas famintos; ela está livre do plano de miséria, de um destino ruim, dos mundos inferiores. </p>

<p class=Normal> (2) - (5) "Outra vez, uma certa pessoa satisfaz o comportamento virtuoso e a concentração mas cultiva a sabedoria de um modo moderado. Com a completa destruição dos cinco primeiros grilhões, essa pessoa realiza nibbana ao pousar ... sem esforço ... com esforço ... destinada a um plano superior, o plano Akanittha. Essa é a quinta pessoa que falece com combustível restante que está livre do inferno, do mundo animal ou do mundo dos fantasmas famintos; ela está livre do plano de miséria, de um destino ruim, dos mundos inferiores. </p>

<p class=Normal> (6) "Outra vez, uma certa pessoa satisfaz o comportamento virtuoso e a concentração mas cultiva a sabedoria de um modo moderado. Com a completa destruição dos três primeiros grilhões, e com a atenuação da cobiça, raiva e delusão, essa pessoa se tornou aquela que retorna uma vez, retornando uma vez a este mundo para dar um fim ao sofrimento. Essa é a sexta pessoa que falece com combustível restante que está livre do inferno, do mundo animal ou do mundo dos fantasmas famintos; ela está livre do plano de miséria, de um destino ruim, dos mundos inferiores. </p>

<p class=Normal> (7) "Outra vez, uma certa pessoa satisfaz o comportamento virtuoso e a concentração mas cultiva a sabedoria de um modo moderado. Com a completa destruição dos três primeiros grilhões, essa pessoa logrou uma-semente, sendo que depois de renascer uma vez como ser humano, dará um fim ao sofrimento. Essa é a sétima pessoa que falece com combustível restante que está livre do inferno, do mundo animal ou do mundo dos fantasmas famintos; ela está livre do plano de miséria, de um destino ruim, dos mundos inferiores. </p>

<p class=Normal> (8) "Outra vez, uma certa pessoa satisfaz o comportamento virtuoso e a concentração mas cultiva a sabedoria de um modo moderado. Com a completa destruição dos três primeiros grilhões, essa pessoa logrou de família-a-família, sendo que depois de vagar e perambular entre famíias, duas ou três vezes, dará um fim ao sofrimento. Essa é a oitava pessoa que falece com combustível restante que está livre do inferno, do mundo animal ou do mundo dos fantasmas famintos; ela está livre do plano de miséria, de um destino ruim, dos mundos inferiores. </p>

<p class=Normal> (9) "Outra vez, uma certa pessoa satisfaz o comportamento virtuoso e a concentração mas cultiva a sabedoria de um modo moderado. Com a completa destruição dos três primeiros grilhões, essa pessoa logrou sete-vezes-no-máximo, sendo que depois de vagar e perambular entre devas e humanos, no máximo sete vezes, dará um fim ao sofrimento. Essa é a pessoa que falece com combustível restante que está livre do inferno, do mundo animal ou do mundo dos fantasmas famintos; ela está livre do plano de miséria, de um destino ruim, dos mundos inferiores. </p>

<p class=Normal>"Quem, Sariputta, são esses errantes de outras seitas tolos e incompetentes e quem são aqueles que conhecem alguém com combustível restante como  'alguém com combustível restante' e alguém sem combustível restante como 'alguém sem combustível restante'? </p>

<p class=Normal>"Essas nove pessoas, Sariputta, falecendo com combustível restante, estão livres do inferno, do mundo animal ou do mundo dos fantasmas famintos; elas estão livres do plano de miséria, de um destino ruim, dos mundos inferiores. Sariputta, eu não estive inclinado a dar esse discurso do Dhamma para os bhikkhus, bhikkhunis, discípulos leigos, e discípulas leigas. Por qual razão? Por estar preocupado que ao ouvir este discurso do Dhamma eles possam tender para a negligência. No entanto, este discurso do Dhamma foi exposto com o propósito de responder a sua pergunta." </p>

