&quot;Bhikkhus, para qualquer um que diga, ‘Qualquer que seja a forma
pela qual uma pessoa faça kamma, é desse modo que ele será experimentado,’ não há
como viver a vida santa, não há oportunidade para o correto fim do sofrimento.
Mas para qualquer um que diga, ‘Quando uma pessoa faz kamma para ser sentido de
tal e tal forma, assim é como o seu resultado é experimentado,’ há como viver a
vida santa, há oportunidade para o correto fim do sofrimento.<o:p></o:p></span></p>


<p class=Normal>
&quot;Há o caso em que um ato insignificante cometido por um certo
indivíduo o conduz ao inferno. Há o caso em que o mesmo tipo de ato
