<p class=Normal> Assim, ouvi. Certa ocasião, o Abençoado estava em Savatthi, no Bosque de Jeta, no Parque de Anathapindika. Agora, naquela ocasião, o Abençoado era respeitado, reverenciado, honrado, venerado, e homenageado, e era um dos que obtinham mantos, comida esmolada, moradias, e medicamentos. A Sangha dos bhikkhus também era respeitada, reverenciada, honrada, venerada, e homenageada, e também obtinham mantos, comida esmolada, moradias, e medicamentos. Mas os errantes de outras seitas não eram respeitados, reverenciados, honrados, venerados, e homenageados, e também não obtinham mantos, comida esmolada, moradias, e medicamentos. Então, os errantes de outras seitas, incapazes de suportar o respeito demonstrado ao Abençoado e à Sangha dos bhikkhus, ao verem os bhikkhus na floresta ou no vilarejo eles os insultavam, ofendiam, provocavam, e irritavam com insultos e palavras grosseiras.</p>

<p class=Normal> Então um número de bhikkhus foram até o Abençoado e depois de cumprimentá-lo sentaram a um lado e disseram: "Agora, venerável senhor, o Abençoado é respeitado ... A Sangha dos bhikkhus também é respeitada ... Mas os errantes de outras seitas não são respeitados ... E agora, venerável senhor, os errantes de outras seitas, incapazes de suportar o respeito demonstrado ao Abençoado e à Sangha dos bhikkhus, ao verem os bhikkhus na floresta ou no vilarejo eles os insultam, ofendem, provocam, e irritam com insultos e palavras grosseiras."</p>

<p class=Normal>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p>

<p class=Normal style='margin-left:.75in'> Quando afetados pelo prazer ou pela dor no vilarejo ou na floresta, <br>
não tomem isso como seu ou de outros. <br>
Os contatos afetam os dependentes do apego. <br>
Como podem os contatos afetar quem não tem apego?</p>

