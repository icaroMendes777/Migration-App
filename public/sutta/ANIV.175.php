<p class=Normal> Então o venerável Upavana foi até o venerável Sariputta e depois de cumprimentá-lo sentou a um lado e disse: </p>

<p class=Normal> <a name=R1></a> (1) "Amigo Sariputta, alguém se torna aquele que dá um fim através do conhecimento?" [<a href="#N1">1</a>]</p>

<p class=Normal> "Esse não é o caso, amigo." </p>

<p class=Normal> (2) "Então, alguém se torna aquele que dá um fim através da conduta?" </p>

<p class=Normal> "Esse não é o caso, amigo." </p>

<p class=Normal> (3) "Então, alguém se torna aquele que dá um fim através do conhecimento e da conduta?" </p>

<p class=Normal> "Esse não é o caso, amigo." </p>

<p class=Normal> (3) "Então, alguém se torna aquele que dá um fim através de outra forma que o conhecimento e a conduta?" </p>

<p class=Normal> "Esse não é o caso, amigo." </p>

<p class=Normal> "Quando você foi perguntado: ' Amigo Sariputta, alguém se torna aquele que dá um fim através do conhecimento?' você respondeu: ' Esse não é o caso, amigo.' Quando você foi perguntado: ' Então, alguém se torna aquele que dá um fim através da conduta? ... através do conhecimento e da conduta? ... de outra forma que o conhecimento e a conduta?' [em cada caso] você respondeu: ' Esse não é o caso, amigo.' Então de que forma alguém se torna aquele que dá um fim?" </p>

<p class=Normal> (1) "Amigo, se alguém se tornasse aquele que dá um fim através do conhecimento, então mesmo aquele que ainda tem apego se tornaria aquele que dá um fim. (2) Se alguém se tornasse aquele que dá um fim através da conduta, então mesmo aquele que ainda tem apego se tornaria aquele que dá um fim. (3) Se alguém se tornasse aquele que dá um fim através do conhecimento e da conduta, então mesmo aquele que ainda tem apego se tornaria aquele que dá um fim. (4) Se alguém se tornasse aquele que dá um fim através de outra forma que o conhecimento e a conduta, então mesmo uma pessoa comum se tornaria aquele que dá um fim; pois uma pessoa comum não possui conhecimento e conduta. </p>

<p class=Normal> "Amigo, quem é deficiente em conduta não compreende e vê as coisas como elas na verdade são. Quem é consumado em conduta compreende e vê as coisas como elas na verdade são. Compreendendo e vendo as coisas como elas na verdade são alguém se torna aquele que dá um fim. [<a href="#N2">2</a>]</p>

