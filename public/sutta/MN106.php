<p class=Normal><a name=N3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[3] MA explica: “tendo superado o mundo da esfera
sensual e tendo decidido com uma mente que tem nos jhanas o seu objetivo.” [<a
