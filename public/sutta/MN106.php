<p class=Normal><a name=N4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[4] MA explica a frase “a sua mente adquire
confiança nessa base” com o significado de que ele alcança ou o insight que tem
como objetivo o estado de arahant, ou o acesso ao quarto jhana. Se ele obtiver
o acesso ao quarto jhana, isto se torna a sua base para alcançar o
“imperturbável,” isto é o quarto jhana em si. Mas se ele obtiver o insight,
então ele decide pelo aperfeiçoamento da sabedoria através do aprofundamento do
insight para alcançar o estado de arahant. A decisão pelo “aperfeiçoamento da
sabedoria” pode explicar porque tantos versos deste sutta, embora culminando
com as realizações advindas da concentração, estão expressas através de frases
apropriadas para explicar o desenvolvimento do insight. [<a href="#R4">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N5></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[5]<span style="mso-spacerun: yes">  </span>MA
explica que este trecho descreve o processo de renascimento de alguém que não
conseguiu alcançar o estado de arahant depois de ter alcançado o quarto jhana.
A “consciência conduzindo ao renascimento”, (<i>samvattanikam viññanam</i>), é
a consciência resultante através da qual aquele ser renasce, e ela possui a
mesma natureza imperturbável da consciência produtora de kamma que alcançou o
quarto jhana. Como a consciência do quarto jhana é que irá determinar o
renascimento, este ser irá renascer num mundo celestial correspondente ao
quarto jhana.</p>
<p class=Normal>Na versão paralela dos Agamas em chinês (MA 75) aparece este trecho:<br>

"Em um momento posterior, com a dissolução do corpo e o fim da vida, devido a essa anterior inclinação mental, ele certamente irá realizar o imperturbável."<br>
Não há menção no MA 75 da expressão "a “consciência conduzindo ao renascimento”, (<i>samvattanikam viññanam</i>)".  [<a href="#R5">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[6] MA diz que esta é a reflexão de alguém que
alcançou o quarto jhana. Como ele inclui a forma material entre as coisas a
serem superadas, se ele alcançar o imperturbável, terá alcançado a base do
espaço infinito, e se ele não alcançar o estado de arahant irá renascer no mundo
do espaço infinito. [<a href="#R6">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N7></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[7] MA diz que esta é a reflexão de alguém que
alcançou a base do espaço infinito. Se ele alcançar o imperturbável, terá
alcançado a base da consciência infinita e irá renascer naquele mundo, se não
tiver alcançado o estado de arahant. [<a href="#R7">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N8></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[8] Esta é a reflexão de alguém que alcançou a
base da consciência infinita e tem como objetivo alcançar a base do nada. [<a
href="#R8">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N9></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[9] MA chama isto de vacuidade com duas pontas
– a ausência de um “eu” e “meu” – e diz que este ensinamento sobre a base do
nada é exposto por meio do insight ao invés da concentração, que foi a
abordagem da seção anterior. No <a href="MN43.php#T33">MN 43.33</a>, é dito que
esta contemplação conduz à libertação da mente através da vacuidade. [<a
href="#R9">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N10></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[10] MA chama isto de vacuidade com quatro
pontas e explica da seguinte forma: (i) ele não vê o seu eu em nenhum lugar;
(ii) ele não vê um eu que lhe pertença que possa ser tratado como algo que
pertence a outrem, exemplo, como um irmão, amigo, assistente, etc.; (iii) ele
não vê o eu de outrem; (iv) ele não vê o eu de outrem que possa ser tratado
como algo que pertença a ele. </span><span style='mso-bidi-font-weight:bold'></p>

<p class=Normal>Veja o <a href="ANVII.55.php">AN VII.55</a>. [<a
href="#R10">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N11></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[11] MA explica: “Se o ciclo de kamma não
houvesse sido acumulado por mim, agora não existiria para mim o ciclo de
resultados; se o ciclo de kamma não for acumulado por mim agora, no futuro não
haverá o ciclo de resultados.” “O que existe, o que veio a ser” são os cinco
agregados. </span><span style='mso-bidi-font-weight:bold'>[<a href="#R11">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N12></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[12] MA diz que ele obtém a equanimidade do
insight, mas considerando o verso 11 parece que também se tem em mente a
