<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[2] Veja o <a href="MN105.php#N6">MN 105 –
nota 6</a>, aqui também parece que o termo “imperturbável” abrange só o quarto
