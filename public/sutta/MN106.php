<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Em certa
ocasião o Abençoado estava entre os Kurus numa cidade denominada
Kammasadhamma. Lá ele se dirigiu aos monges
desta forma: &quot;Bhikkhus.&quot; – &quot;Venerável Senhor,&quot; eles
responderam. O Abençoado disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.
“Bhikkhus, os prazeres sensuais [<a href="#N1">1</a>] <a name=R1></a> são
impermanentes, vazios, falsos, enganosos; eles são ilusórios, a tagarelice dos
tolos. Os prazeres sensuais aqui e agora e os prazeres sensuais nas vidas que
virão, as percepções sensuais aqui e agora e as percepções sensuais nas vidas
que virão – ambos são igualmente o reino de Mara, o domínio de Mara, o engodo
de Mara, a reserva de caça de Mara. Por conta deles, estes estados ruins e
prejudiciais como a cobiça, má vontade e a presunção surgem, e eles constituem
uma obstrução para o nobre discípulo que aqui se encontra em treinamento.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(O
IMPERTURBÁVEL)</b> [<a href="#N2">2</a>] <a name=R2></a><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. “Nesse
sentido, bhikkhus, um nobre discípulo considera o seguinte: ‘Os prazeres
sensuais aqui e agora e os prazeres sensuais nas vidas que virão ... constituem
uma obstrução para o nobre discípulo que aqui se encontra em treinamento. E se
eu permanecesse com a mente abundante e transcendente, tendo superado o mundo e
tomado uma firme decisão com a mente. [<a href="#N3">3</a>] <a name=R3></a>Quando
eu fizer isso, não haverá mais estados mentais ruins e prejudiciais em mim como
a cobiça, má vontade e presunção, e com o abandono destes a minha mente estará
ilimitada, imensurável e bem desenvolvida.’ Ao praticar dessa forma e
permanecer assim freqüentemente, a sua mente adquire confiança nessa base. [<a
href="#N4">4</a>] <a name=R4></a>Uma vez que haja completa confiança, ele ou
alcança o imperturbável agora, ou então decide pelo aperfeiçoamento da
sabedoria. Na dissolução do corpo, após a morte, é possível que a consciência
dele, conduzindo ao renascimento, possa passar para o imperturbável. [<a
href="#N5">5</a>] <a name=R5></a>Este, bhikkhus, é declarado o primeiro caminho
dirigido para o imperturbável.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4.
“Novamente, bhikkhus, um nobre discípulo considera o seguinte:[<a href="#N6">6</a>]
<a name=R6></a>‘Existem prazeres sensuais aqui e agora e prazeres sensuais nas
vidas que virão, percepções sensuais aqui e agora e percepções sensuais nas
vidas que virão; qualquer que seja a forma material, toda forma material
compreende os quatro grandes elementos e a forma material derivada dos quatro
grandes elementos.’ Ao praticar dessa forma e permanecer assim com freqüência,
a sua mente adquire confiança nessa base. Uma vez que haja completa confiança,
ele ou alcança o imperturbável agora, ou então decide pelo aperfeiçoamento da sabedoria.
Na dissolução do corpo, após a morte, é possível que a consciência dele,
conduzindo ao renascimento, possa passar para o imperturbável. Este, bhikkhus,
é declarado o segundo caminho dirigido ao imperturbável. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5.
“Novamente, bhikkhus, um nobre discípulo considera o seguinte:[<a href="#N7">7</a>]
<a name=R7></a>‘Prazeres sensuais aqui e agora e prazeres sensuais nas vidas
que virão, percepções sensuais aqui e agora e percepções sensuais nas vidas que
virão, formas materiais aqui e agora e formas materiais nas vidas que virão,
percepções das formas aqui e agora e percepções das formas nas vidas que virão
– ambas são igualmente impermanentes. Aquilo que é impermanente, não vale a
pena se deliciar nisso, não vale a pena receber isso, não vale a pena se agarrar nisso.’
Ao praticar dessa forma e permanecer assim freqüentemente, a sua mente adquire
confiança nessa base. Uma vez que haja completa confiança, ele ou alcança o
imperturbável agora, ou então decide pelo aperfeiçoamento da sabedoria. Na
dissolução do corpo, após a morte, é possível que a consciência dele,
conduzindo ao renascimento, possa passar para o imperturbável. Este, bhikkhus,
é declarado o terceiro caminho dirigido ao imperturbável.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(A BASE DO
NADA)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6.
“Novamente, bhikkhus, um nobre discípulo considera o seguinte: [<a href="#N8">8</a>]
<a name=R8></a>‘Prazeres sensuais aqui e agora e prazeres sensuais nas vidas
que virão, percepções sensuais aqui e agora e percepções sensuais nas vidas que
virão, formas materiais aqui e agora e formas materiais nas vidas que virão,
percepções das formas aqui e agora e percepções das formas nas vidas que virão,
e percepções do imperturbável – são todas percepções. Onde todas essas
percepções cessam sem vestígios, isso é a paz, isso é o sublime, isto é, a base
do nada.’ Ao praticar dessa forma e permanecer assim freqüentemente, a sua
mente adquire confiança nessa base. Uma vez que haja completa confiança, ele ou
alcança o imperturbável agora, ou então decide pelo aperfeiçoamento da
sabedoria. Na dissolução do corpo, após a morte, é possível que a consciência
dele, conduzindo ao renascimento, possa passar para a base do nada. Este,
bhikkhus, é declarado o primeiro caminho dirigido à base do nada.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7.
“Novamente, bhikkhus, um nobre discípulo, dirigindo-se à floresta, ou à sombra de uma árvore, ou a um local isolado, considera o seguinte: ‘Isto está vazio de
um eu ou daquilo que pertence a um eu.’ [<a href="#N9">9</a>] <a name=R9></a>Ao
praticar dessa forma e permanecer assim freqüentemente, a sua mente adquire
confiança nessa base. Uma vez que haja completa confiança, ele ou alcança o
imperturbável agora, ou então decide pelo aperfeiçoamento da sabedoria. Na
dissolução do corpo, após a morte, é possível que a consciência dele,
conduzindo ao renascimento, possa passar para a base do nada. Este, bhikkhus, é
declarado o segundo caminho dirigido à base do nada.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8.
“Novamente, bhikkhus, um nobre discípulo considera o seguinte: ‘Isso poderá não ser;  isso poderá não ser meu. Isso não será; isso não será meu. Eu estou abandonando aquilo que existe, que veio a ser.' [<a href="#N10">10</a>] <a name=R10></a>Ao praticar dessa
forma e permanecer assim freqüentemente, a sua mente adquire confiança nesta
base. Uma vez que haja completa confiança, ele ou alcança o imperturbável
agora, ou então decide pelo aperfeiçoamento da sabedoria. Na dissolução do
corpo, após a morte, é possível que a consciência dele, conduzindo ao
renascimento, possa passar para a base do nada. Este, bhikkhus, é declarado o
terceiro caminho dirigido à base do nada.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(BASE DA
NEM PERCEPÇÃO, NEM NÃO PERCEPÇÃO)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9.
“Novamente, bhikkhus, um nobre discípulo considera o seguinte: ‘Prazeres
sensuais aqui e agora e prazeres sensuais nas vidas que virão, percepções
sensuais aqui e agora e percepções sensuais nas vidas que virão, formas
materiais aqui e agora e formas materiais nas vidas que virão, percepções das
formas aqui e agora e percepções das formas nas vidas que virão, percepções do
imperturbável e percepções da base do nada – são todas percepções. Onde todas
essas percepções cessam sem vestígios, isso é a paz, isso é o sublime, isto é,
a base da nem percepção, nem não percepção.’ Ao praticar dessa forma e
permanecer assim freqüentemente, a sua mente adquire confiança nessa base. Uma
vez que haja completa confiança, ele ou alcança a base da nem percepção, nem
não percepção, ou então decide pelo aperfeiçoamento da sabedoria. Na dissolução
do corpo, após a morte, é possível que a consciência dele, conduzindo ao
renascimento, possa passar para a base da nem percepção, nem não percepção.
Este, bhikkhus, é declarado o caminho dirigido à base da nem percepção, nem não
percepção.”<o:p></o:p></span></p>



<p class=Normal><b>(NIBBANA)</b></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. Quando
isso foi dito, o venerável Ananda disse para o Abençoado: “Venerável senhor,
aqui um bhikkhu está praticando assim: ‘Se isso não tivesse sido, isso não
seria meu; isso não será e isso não será meu. O que existe, o que veio a ser,
isso eu estou abandonando.’ [<a href="#N11">11</a>] <a name=R11></a>Dessa forma
ele obtém a equanimidade. [<a href="#N12">12</a>] <a name=R12></a>Venerável
senhor, esse bhikkhu realiza Nibbana?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Um bhikkhu
aqui, Ananda, poderá realizar Nibbana, um outro bhikkhu aqui poderá não
realizar Nibbana.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Qual é a
causa e razão, venerável senhor, porque um bhikkhu aqui poderá realizar
Nibbana, enquanto que outro bhikkhu aqui poderá não realizar Nibbana?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
Ananda, um bhikkhu pratica da seguinte forma: ‘Se isso não tivesse sido, isso
não seria meu; isso não será e isso não será meu. O que existe, o que veio a
ser, isso eu estou abandonando.’ Assim ele obtém a equanimidade. Ele se delicia
nessa equanimidade, a recebe, permanece agarrado a ela. Ao agir assim, a sua
consciência se torna dependente disso e se apega a isso. Um bhikkhu, Ananda,
que está influenciado pelo apego não realiza Nibbana.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. “Mas,
venerável senhor, quando um bhikkhu se apega, a que ele está se apegando?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“À base da
nem percepção, nem não percepção, Ananda.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando um
bhikkhu se apega, venerável senhor, parece que ele se apega ao melhor objeto de
apego.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando
esse bhikkhu se apega, Ananda, ele se apega ao melhor objeto de apego, pois
esse é o melhor objeto de apego, isto é, a base da nem percepção, nem não
percepção. [<a href="#N13">13</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R13></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>12. “Aqui, Ananda, um bhikkhu pratica da seguinte forma: ‘Se isso não
tivesse sido, isso não seria meu; isso não será e isso não será meu. O que
existe, o que veio a ser, isso eu estou abandonando.’ Assim ele obtém a
equanimidade. Ele não se delicia nessa equanimidade, não a recebe, não fica
agarrado a ela. Como ele não age assim, a sua consciência não se torna
dependente disso e não se apega a isso. Um bhikkhu, Ananda, que está sem apego,
realiza Nibbana.”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. “É
maravilhoso, venerável senhor, é admirável! O Abençoado, de fato, nos explicou
como cruzar a torrente na dependência de um apoio ou outro. [<a href="#N14">14</a>]
<a name=R14></a>Mas, venerável senhor, o que é a nobre libertação?” </span>[<a
href="#N15">15</a>]</p>

<p class=Normal><a name=R15></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Aqui, Ananda, um nobre discípulo considera da seguinte forma: ‘Prazeres
sensuais aqui e agora e prazeres sensuais nas vidas que virão, percepções
sensuais aqui e agora e percepções sensuais nas vidas que virão, formas
materiais aqui e agora e formas materiais nas vidas que virão, percepções das
formas aqui e agora e percepções das formas nas vidas que virão, percepções do
imperturbável, percepções da base do nada e percepções da base da nem
percepção, nem não percepção – isso é a identidade até onde se estende a
identidade. [<a href="#N16">16</a>] <a name=R16></a>Isto é o Imortal, isto é, a
libertação da mente através do desapego.’ </span>[<a href="#N17">17</a>]</p>


<p class=Normal><a name=R17></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>14. “Portanto, Ananda, eu ensinei o caminho para o imperturbável, eu
ensinei o caminho para a base do nada, eu ensinei o caminho para a base da nem
percepção, nem não percepção, eu ensinei o caminho para cruzar a torrente na
dependência de um apoio ou outro, eu ensinei a nobre libertação.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. “Aquilo
que por compaixão um Mestre<span style="mso-spacerun: yes">  </span>deveria
fazer para os seus discípulos, desejando o bem-estar deles, isso eu fiz por
você, Ananda. Ali estão aquelas árvores, aquelas cabanas vazias.
Medite, Ananda, não adie, ou então você irá se arrepender mais tarde. Essa é a
nossa instrução para você.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o
que disse o Abençoado. O venerável Ananda ficou satisfeito e contente com as
palavras do Abençoado..<o:p></o:p></span></p>


