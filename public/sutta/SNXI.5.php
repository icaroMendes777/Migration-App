<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, certa vez no passado, os devas e asuras estavam preparados
para a batalha. Então Vepacitti, o senhor dos asuras, disse para Sakka, o
senhor dos devas: ‘Senhor dos devas, que a vitória seja decidida pelo bom
conselho.’ [E Sakka respondeu:] ‘Vepacitti, que a vitória seja decidida pelo
bom conselho.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
bhikkhus, os devas e os asuras nomearam um painel de juízes, dizendo: ‘Eles
irão determinar aquilo que for bem dito e mau dito por nós.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então
Vepacitti, o senhor dos asuras, disse para Sakka, o senhor dos devas: ‘Recite
um verso, senhor dos devas.’ Quando isso foi dito, Sakka disse para Vepacitti:
‘Você, Vepacitti, sendo o deva sênior aqui, recite um verso.’ [<a href="#N1">1</a>]
<a name=R1></a>Quando isso foi dito, Vepacitti, o senhor dos asuras, recitou
este verso: <o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“’Os tolos dariam ainda mais vazão à sua raiva <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>se alguém não discipliná-los.<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Por conseguinte, com drástica punição<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>o homem sábio deve controlar o tolo.’ <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando,
bhikkhus, Vepacitti, o senhor dos asuras, disse esses versos, os asuras
aplaudiram, mas os devas ficaram em silêncio. Então, Vepacitti disse para
Sakka: ‘Diga um verso, senhor dos devas.’ Quando isso foi dito, Sakka, o senhor
dos devas, recitou este verso:<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“’Eu por mim penso que só esta<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>é a forma de deter o tolo: <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>quando alguém sabe que o seu oponente está enraivecido<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>ele com atenção plena se mantém em paz.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando,
bhikkhus, Sakka, o senhor dos devas, disse esses versos, os devas aplaudiram,
mas os asuras ficaram em silêncio. Então, Sakka disse para Vepacitti: ‘Diga um
verso, Vepacitti.’ Quando isso foi dito, Vepacitti, o senhor dos asuras, recitou
este verso:<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“’Eu vejo esse defeito, Oh Vasava,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>na prática da tolerância paciente,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>quando o tolo pensa de você assim,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>“ele me tolera por medo,”<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>o imbecil irá perseguí-lo ainda mais<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>tal qual um touro aquele que corre.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando,
bhikkhus, Vepacitti, o senhor dos asuras, disse esses versos, os asuras
aplaudiram, mas os devas ficaram em silêncio. Então Vepacitti disse para Sakka:
‘Diga um verso, senhor dos devas.’ Quando isso foi dito, Sakka, o senhor dos
devas, recitou estes versos:<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“’Deixe estar, quer ele pense ou não,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>“ele me tolera por medo,”<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>dentre as perfeições que culminam no próprio benefício<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>nenhuma é melhor que a paciência.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“’Quando uma pessoa dotada de força<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>pacientemente tolera alguém mais fraco,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>isso é chamado a suprema paciência;<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>o mais fraco sempre tem que ser paciente.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“’De modo nenhum pode ser chamada de força -<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>a força que é a força da estupidez -<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>mas ninguém pode censurar uma pessoa<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>que é forte porque está guardada pelo Dhamma. [<a href="#N2">2</a>]<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><a name=R2></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>“’Quem repaga um homem enraivecido com a raiva<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>dessa forma piora as coisas para si mesmo.<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Não repagando um homem enraivecido com a raiva,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>ele vence uma batalha difícil de ser vencida.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“’Ele pratica para o bem estar de ambos,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>o seu próprio e dos outros,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>quando, sabendo que o seu oponente está enraivecido,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>ele com atenção plena se mantém em paz.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“’Quando ele alcança a cura para ambos -<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>dele mesmo e dos outros -<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>as pessoas que o consideram um tolo<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>desconhecem o Dhamma.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando,
bhikkhus, Sakka, o senhor dos devas, disse esses versos, os devas aplaudiram,
mas os asuras ficaram em silêncio. Então, o painel de juízes nomeado pelos
devas e pelos asuras disse o seguinte: ‘Os versos ditos por Vepacitti, senhor
dos asuras, fazem parte da esfera da punição e da violência; por conseguinte
[eles envolvem] conflito, disputa e hostilidade. Mas os versos ditos por Sakka,
o senhor dos devas, fazem parte da esfera da não punição e da não violência;
por conseguinte [eles envolvem] a emancipação do conflito, a emancipação da
disputa e a emancipação da hostilidade. Sakka, o senhor dos devas, conquistou a
vitória com o bom conselho.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Dessa
forma, bhikkhus, Sakka, o senhor dos devas, conquistou a vitória com o bom
conselho.”<span style='mso-tab-count:1'>           </span><o:p></o:p></span></p>




