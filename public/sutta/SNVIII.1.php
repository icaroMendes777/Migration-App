<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim ouvi.
Em certa ocasião, o Venerável Vangisa estava em Alavi no Templo Aggalava junto com
o seu preceptor, o Venerável Nigrodhakappa. Agora, naquela ocasião, o Venerável
Vangisa, recém ordenado, que havia não muito tempo tinha deixado a vida em
família, foi deixado como guardião do templo. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, um
grande número de mulheres, muito belas e enfeitadas, se aproximaram do Parque
Aggalavaka para visitar o templo. Quando o Venerável Vangisa viu aquelas
mulheres, a insatisfação surgiu nele; a cobiça infestou a sua mente. Então ele
pensou: “É de fato uma perda para mim, não é um ganho para mim! É de fato um
contratempo para mim, não é um ganho para mim, que a insatisfação tenha surgido
em mim, que a cobiça tenha infestado a minha mente. Como poderia outrem
dissipar a minha insatisfação e despertar o deleite? Que eu mesmo dissipe a
minha própria insatisfação e eu mesmo desperte o deleite.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, o
Venerável Vangisa, tendo dissipado a sua própria insatisfação e tendo despertado
o deleite, naquela ocasião recitou estes versos:<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
