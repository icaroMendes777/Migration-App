<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então,
referindo-se ao jovem deva Candima, o Abençoado se dirigiu em versos a Rahu, o
senhor dos asuras:<span style='mso-tab-count:1'>    </span><o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR style='mso-ansi-language:PT-BR'><span
