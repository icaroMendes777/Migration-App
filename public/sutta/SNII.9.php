por Rahu, o senhor dos asuras. [<a href="#N1">1</a>] <a name=R1></a>Então, recordando-se
do Abençoado, o jovem deva Candima recitou o seguinte verso:<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-tab-count:1'>            </span>“Presto homenagem ao Buda! <br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'><span
