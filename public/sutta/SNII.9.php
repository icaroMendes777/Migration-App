<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então Rahu,
o senhor dos asuras, libertou o jovem deva Candima e apressado foi até Vepacitti,
o senhor dos asuras.[<a href="#N2">2</a>] <a name=R2></a>Ao chegar, chocado e
aterrorizado, ele ficou em pé a um lado. Então, como ele ali permanecesse,
