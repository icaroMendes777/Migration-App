<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, quaisquer Arahants, Perfeitamente Iluminados, que no
passado despertaram completamente para as coisas como elas na verdade são,
todos despertaram para as Quatro Nobres Verdades como elas na verdade são.
Quaisquer Arahants, Perfeitamente Iluminados, que no futuro despertarão
completamente para as coisas como elas na verdade são, todos despertarão para
as Quatro Nobres Verdades como elas na verdade são. Quaisquer Arahants,
Perfeitamente Iluminados, que no presente despertam completamente para as
coisas como elas na verdade são, todos despertam para as Quatro Nobres Verdades
como elas na verdade são.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quais
quatro? A nobre verdade do sofrimento, a nobre verdade da origem do sofrimento,
a nobre verdade da cessação do sofrimento, a nobre verdade do caminho que
conduz à cessação do sofrimento. Quaisquer Arahants, Perfeitamente Iluminados,
que no passado despertaram ... que no futuro despertarão ... que no presente
despertam completamente para as coisas como elas na verdade são, todos
despertam para as Quatro Nobres Verdades como elas na verdade são. <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
bhikkhus, um esforço deve ser feito para compreender: ‘Isto é sofrimento.’ Um
esforço deve ser feito para compreender: ‘Esta é a origem do sofrimento.’ Um
esforço deve ser feito para compreender: ‘Esta é a cessação do sofrimento.’ Um
esforço deve ser feito para compreender: ‘Este é o caminho que conduz à
cessação do sofrimento.’”<o:p></o:p></span></p>

