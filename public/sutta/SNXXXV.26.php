<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, sem o conhecimento direto e completa compreensão do todo, sem
desenvolver o desapego em relação a ele e abandoná-lo, uma pessoa é incapaz de
destruir o sofrimento. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é esse todo que sem o seu conhecimento direto e completa compreensão,
que sem desenvolver o desapego em relação a ele, abandonando-o, uma pessoa é
incapaz de destruir o sofrimento?<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sem o
conhecimento direto e completa compreensão do olho, sem desenvolver o desapego
em relação a ele, abandonando-o, uma pessoa será incapaz de destruir o
sofrimento. Sem o conhecimento direto e completa compreensão das formas ...
da consciência no olho ... do contato no olho ... de qualquer sensação que surja tendo
o contato no olho como condição – quer seja prazerosa, dolorosa ou nem prazerosa, nem dolorosa - sem
desenvolver o desapego em relação a ela, abandonando-a, uma pessoa será incapaz
de destruir o sofrimento.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sem o
conhecimento direto e completa compreensão do ouvido ... do nariz ... da língua ... do corpo ... da mente ... dos objetos mentais ...
da consciência na mente ... do contato na mente ... de
qualquer sensação que surja tendo o contato na mente como condição – quer seja prazerosa, dolorosa ou nem prazerosa, nem dolorosa -  sem desenvolver o desapego em relação a ela,
abandonando-a, uma pessoa será incapaz de destruir o sofrimento.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Esse,
bhikkhus, é o todo que sem o seu conhecimento direto e completa compreensão, sem desenvolver o desapego em relação a ele,
abandonando-o, uma pessoa será incapaz de destruir o sofrimento.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
com o conhecimento direto e completa compreensão do todo, desenvolvendo o
desapego em relação a ele, abandonando-o, uma pessoa é capaz de destruir o
sofrimento.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é esse todo que, com o seu conhecimento direto e completa
compreensão, com o desenvolvimento do desapego em relação a ele, abandonando-o,
uma pessoa é capaz de destruir o sofrimento?<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Com o
conhecimento direto e completa compreensão do olho com o
desenvolvimento do desapego em relação a ele, abandonando-o, uma pessoa é capaz
de destruir o sofrimento. Com o
conhecimento direto e completa compreensão das formas ...
da consciência no olho ... do contato no olho ... de qualquer sensação que surja tendo
o contato no olho como condição – quer seja prazerosa, dolorosa ou nem prazerosa, nem dolorosa -  com o
desenvolvimento do desapego em relação a ela, abandonando-a, uma pessoa é capaz
de destruir o sofrimento. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Com o
conhecimento direto e completa compreensão do ouvido ... do nariz ... da língua ... do corpo ... da mente ... dos objetos mentais ...
da consciência na mente ... do contato na mente ... de qualquer
sensação que surja tendo o contato na mente como condição – quer seja prazerosa, dolorosa ou nem prazerosa, nem dolorosa -  com o
desenvolvimento do desapego em relação a ela, abandonando-a, uma pessoa é capaz
de destruir o sofrimento.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Esse,
bhikkhus, é o todo que com o seu conhecimento direto e a completa compreensão, com o
desenvolvimento do desapego em relação a ele, abandonando-o, uma pessoa é capaz
de destruir o sofrimento.”<o:p></o:p></span></p>


