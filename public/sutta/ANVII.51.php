<p class=Normal>Então um certo bhikkhu foi até o Abençoado e depois de cumprimentá-lo ele sentou a um lado e disse: “Venerável Senhor, qual é a causa, qual é a razão porque a dúvida não surge num nobre discípulo bem instruído com relação às coisas que não foram declaradas?” </p>

 

<p class=Normal> “Devido à cessação das idéias, bhikkhu, a dúvida não surge num nobre discípulo bem instruído com relação às coisas que não foram declaradas. A idéia ‘após a morte um Tathagata existe,’ a idéia ‘após a morte um Tathagata não existe,’ a idéia ‘após a morte um Tathagata tanto  existe como não existe,’ a idéia ‘após a morte um Tathagata nem existe, nem não existe’: a pessoa comum sem instrução não compreende as idéias, não compreende a origem das idéias, não compreende a cessação das idéias, não compreende o caminho da prática que conduz à cessação das idéias, e assim aquela idéia cresce nela. Ela não está livre do nascimento, do envelhecimento e da morte; da tristeza, lamentação, dor, angústia e desespero. Ela não está livre, eu lhes digo, do sofrimento. Mas o nobre discípulo bem instruído compreende as idéias, compreende a origem das idéias, compreende a cessação das idéias, compreende o caminho da prática que conduz à cessação das idéias, e assim aquela idéia não cresce nele. Ele está livre do nascimento, do envelhecimento e da morte; da tristeza, lamentação, dor, angústia e desespero. Ele está livre, eu lhes digo, do sofrimento. </p>

 

<p class=Normal> “Assim compreendendo, assim vendo, o nobre discípulo bem instruído não declara que ‘após a morte um Tathagata existe,’ não declara que ‘após a morte um Tathagata não existe,’ não declara que ‘após a morte um Tathagata tanto existe como não existe,’ não declara que ‘após a morte um Tathagata nem existe, nem não existe.’ Compreendendo dessa forma, vendo dessa forma, ele assim tem o caráter de não declarar as coisas não declaradas. Compreendendo dessa forma, vendo dessa forma, ele não fica paralisado, não treme, não se abala ou fica agitado por coisas não declaradas. </p>

 

<p class=Normal> “‘Após a morte um Tathagata existe,’ esse ponto de vista com base no desejo, esse ponto de vista com base na percepção, esse produto da concepção, esse produto da fabricação, esse ponto de vista com base no apego: isso é aflição. [<a href="#N1">1</a>] <a name="R1"> </a> ‘Após a morte um Tathagata não existe’: ... isso é aflição. ‘Após a morte um Tathagata tanto existe como não existe’: ... isso é aflição. ‘Após a morte um Tathagata nem existe, nem não existe’: ... isso é aflição. </p>

 

<p class=Normal>A pessoa comum sem instrução não compreende a aflição, não compreende a origem da aflição, não compreende a cessação da aflição, não compreende o caminho da prática que conduz à cessação da aflição, e assim aquela aflição cresce nela. Ela não está livre do nascimento, do envelhecimento e da morte; da tristeza, lamentação, dor, angústia e desespero. Ela não está livre, eu lhes digo, do sofrimento. Mas o nobre discípulo bem instruído compreende a aflição, compreende a origem da aflição, compreende a cessação da aflição, compreende o caminho da prática que conduz à cessação da aflição, e assim aquela idéia não cresce nele. Ele está livre do nascimento, do envelhecimento e da morte; da tristeza, lamentação, dor, angústia e desespero. Ele está livre, eu lhes digo, do sofrimento. </p>

 

<p class=Normal> “Assim compreendendo, assim vendo, o nobre discípulo bem instruído não declara que ‘após a morte um Tathagata existe,’ não declara que ‘após a morte um Tathagata não existe,’ não declara que ‘após a morte um Tathagata como existe como não existe,’ não declara que ‘após a morte um Tathagata nem existe, nem não existe.’ Compreendendo dessa forma, vendo dessa forma, ele assim tem o caráter de não declarar as coisas não declaradas. Compreendendo dessa forma, vendo dessa forma, ele não fica paralisado, não treme, não se abala ou fica agitado por coisas não declaradas.” </p>

