<p class=Normal>
“ Existem quatro tipos de nuvens carregadas de chuva. Quais quatro?
Aquela que troveja porém não chove, aquela que chove porém não troveja, aquela
que não troveja nem chove, e aquela que troveja e chove. Esses são os quatro
tipos de nuvens carregadas de chuva.<o:p></o:p></span></p>



<p class=Normal>
“ Da mesma forma, existem quatro tipos de pessoas que podem ser
encontradas no mundo que se assemelham a nuvens carregadas de chuva. Quais quatro?
Aquela que troveja porém não chove, aquela que chove porém não troveja, aquela
que não troveja nem chove, e aquela que troveja e chove. <o:p></o:p></span></p>



<p class=Normal>
“ E como é o tipo de pessoa que troveja porém não chove? É o caso em
que a pessoa domina o Dhamma: sumários, prosa e verso, análises, versos, exclamações, símiles, histórias de vidas passadas, eventos maravilhosos, perguntas e respostas [ a
classificação mais antiga dos ensinamentos do Buda]. No entanto ela não compreende
como as coisas na verdade são, que ‘Isto é sofrimento’. Ela não compreende
como as coisas na verdade são, que ‘Esta é a origem do sofrimento’. Ela não
compreende como as coisas na verdade são, que ‘Esta é a cessação do
sofrimento’. Ela não compreende como as coisas na verdade são, que ‘Este é o
caminho da prática que conduz à cessação do sofrimento’. Esse é o tipo de
pessoa que troveja mas não chove. Esse tipo de pessoa, lhes digo, é como a
nuvem que troveja mas não chove.<o:p></o:p></span></p>



<p class=Normal>
“ E como é o tipo de pessoa que chove porém não troveja? É o caso em
que a pessoa não domina o Dhamma: sumários ... perguntas e respostas.
No entanto ela compreende como as coisas na verdade são, que<span
style='mso-spacerun:yes'>  </span>‘Isto é sofrimento’. Ela compreende como as
coisas na verdade são, que ‘Esta é a origem do sofrimento’. Ela compreende
como as coisas na verdade são, que ‘Esta é a cessação do sofrimento’. Ela<span
style='mso-spacerun:yes'>  </span>compreende como as coisas na verdade são, que
‘Este é o caminho da prática que conduz à cessação do sofrimento’. Esse é o tipo
de pessoa que chove mas não troveja. Esse tipo de pessoa, lhes digo, é como a
nuvem que chove mas não troveja.<o:p></o:p></span></p>



<p class=Normal>
“ E como é o tipo de pessoa que não chove nem troveja? É o caso em que
a pessoa não domina o Dhamma: sumários ... perguntas e respostas. E
ela também não compreende como as coisas na verdade são, que<span
style='mso-spacerun:yes'>  </span>‘Isto é sofrimento’ ... ‘Esta é a origem do
sofrimento’ ... ‘Esta é a cessação do sofrimento’ ... ‘Este é o caminho da
prática que conduz à cessação do sofrimento’. Esse é o tipo de pessoa que não
chove nem troveja. Esse tipo de pessoa, lhes digo, é como a nuvem que não chove
nem troveja.<o:p></o:p></span></p>



<p class=Normal>
“ E como é o tipo de pessoa que chove e troveja? É o caso em que a
pessoa domina o Dhamma: sumários ... perguntas e respostas. E ela
também compreende como as coisas na verdade são, que<span
style='mso-spacerun:yes'>  </span>‘Isto é sofrimento’ ... ‘Esta é a origem do
sofrimento’ ... ‘Esta é a cessação do sofrimento’ ...‘Este é o caminho da
prática que conduz à cessação do sofrimento’. Esse é o tipo de pessoa que chove
e troveja. Esse tipo de pessoa, lhes digo, é como a nuvem que chove e troveja.<o:p></o:p></span></p>



<p class=Normal>
“ Esses são os quatro tipos de pessoas que podem ser encontradas no
mundo.<o:p></o:p></span></p>

