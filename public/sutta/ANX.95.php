<p class=Normal>Então o errante Uttiya foi até o Abençoado e ao chegar ambos se cumprimentaram. Depois da troca de saudações amigáveis e corteses ele sentou a um lado e disse para o Abençoado: </p>

<p class=Normal> “Mestre Gotama, é o caso que ‘O mundo é eterno. Somente isso é verdadeiro; todo o restante é falso’?” </p>

<p class=Normal> “Uttiya, eu não declarei que ‘O mundo é eterno. Somente isso é verdadeiro; todo o restante é falso.’” </p>

<p class=Normal> “Muito bem, então Mestre Gotama, é o caso que ‘O mundo não é eterno. Somente isso é verdadeiro; todo o restante é falso’?” </p>

<p class=Normal> “Uttiya, eu não declarei que ‘O mundo não é eterno. Somente isso é verdadeiro; todo o restante é falso.’” </p>

<p class=Normal> “Muito bem, então Mestre Gotama, é o caso que ‘O mundo é finito ...’ ‘O mundo é infinito ...’ ‘A alma e o corpo são a mesma coisa  ...’ ‘A alma é uma coisa e o corpo outra ...’ ‘Após a morte um Tathagata existe ...’ ‘Após a morte um Tathagata não existe ...’ ‘Após a morte um Tathagata tanto existe como não existe ...’ ‘Após a morte um Tathagata nem existe nem não existe. Somente isso é verdadeiro; todo o restante é falso.’” </p>

<p class=Normal> “Uttiya, eu não declarei que ‘Após a morte um Tathagata nem existe nem não existe. Somente isso é verdadeiro; todo o restante é falso.’” </p>

<p class=Normal> “Mas, Mestre Gotama, ao ser perguntado, ‘É o caso que “O mundo é eterno. Somente isso é verdadeiro; todo o restante é falso”?’ você me responde, ‘Uttiya, eu não declarei que “O mundo é eterno. Somente isso é verdadeiro; todo o restante é falso.”’ Ao ser perguntado ‘É o caso que “O mundo não é eterno ...  O mundo é finito  ... O mundo é infinito ...  A alma e o corpo são a mesma coisa ...  A alma é uma coisa e o corpo outra ...   Após a morte um Tathagata existe ...  Após a morte um Tathagata não existe ...  Após a morte um Tathagata tanto existe como não existe ...  Após a morte um Tathagata nem existe nem não existe. Somente isso é verdadeiro; todo o restante é falso”?’  você me responde, ‘Uttiya, eu não declarei que “Após a morte um Tathagata nem existe nem não existe. Somente isso é verdadeiro; todo o restante é falso.”’ Agora há alguma coisa que você tenha declarado?” </p>

<p class=Normal> “Uttiya, tendo compreendido através do conhecimento direto, eu ensino o Dhamma para os meus discípulos para a purificação dos seres, para a superação da tristeza e lamentação, para o desaparecimento da dor e da angústia, para a realização do caminho correto e para a realização de nibbana.” </p>

<p class=Normal> “E, Mestre Gotama, tendo compreendido através do conhecimento direto, você  ensina o Dhamma para os seus discípulos para a purificação dos seres, para a superação da tristeza e lamentação, para o desaparecimento da dor e da angústia, para a realização do caminho correto e para a realização de nibbana, todo o mundo será conduzido para a libertação, ou apenas metade, ou um terço?” </p>

<p class=Normal>Quando isso foi dito, o Abençoado ficou em silêncio. </p>

<p class=Normal>Então o seguinte pensamento ocorreu ao ven. Ananda: “Não devo permitir que o errante Uttiya conceba uma opinião prejudicial assim, ‘Quando perguntei ao contemplativo Gotama uma questão abrangente, ele vacilou e não respondeu. Talvez ele não seja capaz de responder.’ Pois isso lhe traria dano e sofrimento por muito tempo.” Assim ele disse para Uttiya, “Nesse caso, meu amigo, eu explicarei com um símile pois alguns sábios compreendem o significado de um enunciado através de um símile. </p>

<p class=Normal> “Suponha que um rei tivesse uma cidade fronteiriça com sólidas muralhas, proteções e abóbadas e com um único portão. O guardião ali postado seria sábio, competente e inteligente; alguém que não permite a entrada de desconhecidos e admite a entrada dos conhecidos. Enquanto ele patrulha, seguindo o caminho que circunda a cidade, ele não vê uma fissura ou abertura nas muralhas, grande o suficiente mesmo para permitir que um gato passe. Ele poderia pensar: ‘Quaisquer criaturas com bom tamanho que entrem ou saiam desta cidade, todas entram e saem através deste portão.’ </p>

<p class=Normal> “Do mesmo modo, o Tathagata não se empenha para que todo o mundo, ou a metade, ou um terço do mundo seja conduzido para a libertação através do seu Dhamma. Mas isto ele sabe: ‘Todos aqueles que foram conduzidos, são conduzidos, ou serão conduzidos para a libertação do mundo assim fizeram, fazem, ou farão, depois de ter abandonado os cinco obstáculos, essas corrupções da mente que enfraquecem a sabedoria, estabelecendo as suas mentes nos quatro fundamentos da atenção plena e tendo de fato desenvolvido os sete fatores da iluminação. Quando você fez essa pergunta ao Abençoado, você já tinha perguntado isso antes de um outro modo. [<a href="#N1">1</a>] Foi por isso que ele não respondeu.” </p>

