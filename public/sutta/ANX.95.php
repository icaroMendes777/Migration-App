<p class=Normal><b>Notas:</b></p>

<p class=Normal><a name="N1"> </a> [1] A questão se todo o mundo ou apenas parte dele será conduzido à libertação é uma outra forma de perguntar se o mundo é eterno ou não. Observe que o ven. Ananda menciona apenas aqueles libertados do mundo, ele não diz que o mundo será conduzido à libertação. </p>

