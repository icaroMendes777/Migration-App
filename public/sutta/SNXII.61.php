<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim ouvi.
Em certa ocasião o Abençoado estava vivendo em Savatthi no Bosque de Jeta, no
Parque de Anathapindika. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
a pessoa comum sem instrução poderá experimentar o desencantamento em relação a
este corpo composto dos quatro grandes elementos; ela poderá se tornar
desapegada dele e libertada dele. Por que razão? Porque o crescimento e o
declínio podem ser vistos neste corpo composto dos quatro grandes elementos,
ele é visto sendo tomado e deixado de lado. Portanto, a pessoa comum sem
instrução poderá experimentar o desencantamento em relação a este corpo
composto dos quatro grandes elementos; ela poderá se tornar desapegada dele e
libertada dele.<span style="mso-spacerun: yes">  </span><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas,
bhikkhus, quanto àquilo que é chamado ‘mente’ ou ‘mentalidade’ ou ‘consciência’ –
a pessoa comum sem instrução é incapaz de experimentar o desencantamento em
relação a isso, incapaz de se tornar desapegada e libertada disso. Por que
razão? Porque, durante muito tempo, isso foi digno da confiança dela, foi
tomado como propriedade dela e compreendido por ela da seguinte forma: ‘Isso é
meu, isso sou eu, isso é o meu eu.’ Portanto, a pessoa comum sem instrução é
incapaz de experimentar o desencantamento em relação à mente, incapaz de se
tornar desapegada disso e libertada disso.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Seria
melhor, bhikkhus, que uma pessoa comum sem instrução tomasse como o eu este
corpo composto dos quatro grandes elementos ao invés da mente. Por que razão?
Porque este corpo composto dos quatro grandes elementos é visto durando um ano,
dois anos, três, quatro, cinco ou dez anos, vinte, trinta, quarenta, cinqüenta
ou sessenta anos, cem anos ou até mais. [<a href="#N1">1</a>] <a name=R1></a>Mas
aquilo que é chamado ‘mente’ ou ‘mentalidade’ ou ‘consciência’ surge como uma
coisa e cessa como outra de dia e de noite. Como um macaco que, vagando pela
floresta, agarra um galho, solta deste e agarra outro, solta deste e agarra um
outro mais, da mesma forma aquilo que é chamado ‘mente’ ou<span
style="mso-spacerun: yes">  </span>‘mentalidade’ ou ‘consciência’ surge como uma
coisa e cessa como outra, seja de dia ou de noite. [<a href="#N2">2</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Nesse sentido, bhikkhus, o nobre discípulo bem instruído observa com
cuidado e atenção a origem dependente da seguinte forma: Quando existe isso,
aquilo existe; Com o surgimento disso, aquilo surge. Quando não existe isso, </span>aquilo
também não existe<span lang=PT-BR style='mso-ansi-language:PT-BR'>; Com a
cessação disto, aquilo cessa. Isto é, com a ignorância como condição, as
formações volitivas [surgem]; com as formações volitivas como condição, a consciência .... Essa é a
origem de toda essa massa de sofrimento.<span style="mso-spacerun: yes"> 
</span>Com o desaparecimento e cessação sem deixar vestígios da ignorância,
cessam as formações volitivas; com a cessação das formações volitivas, cessa a consciência. Essa é
a cessação de toda essa massa de sofrimento.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Vendo
dessa forma, bhikkhus, o nobre discípulo bem instruído experimenta o
desencantamento em relação à forma, o desencantamento em relação à sensação, o
desencantamento em relação à percepção, o desencantamento em relação às
formações, o desencantamento em relação à consciência. Experimentando o
desencantamento ele se desapega. Através do desapego ele se liberta. Quando ele
está libertado surge o conhecimento: ‘Libertado.’ Ele compreende que: ‘O
nascimento foi destruído, a vida santa foi vivida, o que deveria ser feito foi
feito, não há mais vir a ser a nenhum estado.’”<o:p></o:p></span></p>




