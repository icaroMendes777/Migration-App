<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] Por que o Buda diz isso? Não é verdade que a forma física sofre
mudanças ao longo da vida e que as formações volitivas estão sujeitas à cessação
imediata? Isso é verdade, mas<span style="mso-spacerun: yes">  </span>diz-se que
o corpo dura um longo tempo numa seqüência contínua, do mesmo modo que se diz
que uma lamparina arde toda a noite como um contínuo conectado, muito embora a
chama cesse exatamente no ponto em que arde sem passar para o próximo pedaço do
pavio. [<a href="#R1">Retorna</a>] <o:p></o:p></span></p>


<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[2] O significado disto é que a mente que surge e cessa durante o dia é
distinta daquela que surge e cessa à noite. “Dia e noite” é mencionado com o
sentido de continuidade, tomando uma continuidade de um período de duração menor
do que o anterior (do corpo). Mas um momento de consciência não dura um dia ou
uma noite toda, num instante uma infinidade de momentos de consciência surgem e
cessam. O símile do macaco não deve ser interpretado como a mente sem
treinamento inquieta a todo momento, mas como a mente sempre dependente de
um objeto. Como o macaco que vagueia pela floresta agarrando de galho em galho,
a mente vagueia pelo mundo dos objetos sempre se agarrando a um deles, quer
seja um objeto visual, um som, qualquer coisa do passado ou do futuro, etc. [<a
href="#R2">Retorna</a>] <b style='mso-bidi-font-weight:normal'><o:p></o:p></b></span></p>




