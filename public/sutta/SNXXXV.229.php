<p class=Normal> “Bhikkhus, a pessoa comum sem instrução fala ‘o oceano, o oceano.’ Mas isso não é o oceano na Disciplina dos Nobres: isso é apenas uma grande massa de água, uma grande extensão de água.</p>

<p class=Normal> "Bhikkhus, há formas conscientizadas através do olho que são desejáveis, agradáveis e fáceis de serem gostadas, conectadas com o desejo sensual. Isso é chamado de oceano na Disciplina dos Nobres. Aqui este mundo com os seus devas, Maras e Brahmas, nesta população com seus brâmanes e contemplativos, seus príncipes e povo, na sua maior parte está submersa, tornaram-se como um novelo embaraçado, como uma corda embolada cheia de nós, como juncos enredados, e não conseguem escapar da transmigração, dos planos de miséria, dos destinos ruins, dos mundos inferiores.</p>

<p class=Normal> "Há sons conscientizados através do ouvido ... objetos mentais conscientizados através da mente que são desejáveis, agradáveis e fáceis de serem gostadas, conectadas com o desejo sensual. Isso é chamado de oceano na Disciplina dos Nobres. Aqui este mundo com os seus devas, Maras e Brahmas, nesta população com seus brâmanes e contemplativos, seus príncipes e povo, na sua maior parte está submersa, tornaram-se como um novelo embaraçado, como uma corda embolada cheia de nós, como juncos enredados, e não conseguem escapar da transmigração, dos planos de miséria, dos destinos ruins, dos mundos inferiores.</p>

<p class=Normal>Isso foi o

que o Abençoado disse. Tendo dito isso, o Iluminado, o Mestre, disse mais:</p>

<p class=Normal style='margin-left:.75in'>Aquele que destruiu a cobiça e a raiva<br>
juntamente com a impureza da ignorância,<br>
atravessou esse oceano tão difícil de ser cruzado<br>
com os seus perigos de tubarões, demônios e ondas.</p>

<p class=Normal style='margin-left:.75in'>Superou o apego, deixou a morte para trás, livre de aquisições,<br>
abandonou o sofrimento, sem uma renovada existência.<br>
Tendo falecido, ele não pode ser medido, eu digo:<br>
ele confundiu o senhor da morte.”</p>

