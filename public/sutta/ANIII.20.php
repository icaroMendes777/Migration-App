<p class=Normal> "Bhikkhus, possuindo três fatores, um lojista em pouco tempo obtém vasta e abundante riqueza. Quais três? Aqui, um lojista tem olhos aguçados, é responsável e tem patrocinadores. </p>

<p class=Normal>"E como, bhikkhus, um lojista tem olhos aguçados? Aqui, um lojista sabe com relação a um determinado artigo: 'Se este artigo for comprado a tal preço e vendido por tal preço irei necessitar esse tanto de capital e realizarei este tanto de lucro.' É desse modo que um lojista tem olhos aguçados. </p>

<p class=Normal>"E como um lojista é responsável? Aqui, um lojista tem habilidade em comprar e vender mercadorias. É desse modo que um lojista é responsável. </p>

<p class=Normal>"E como um lojista tem patrocinadores? Aqui, chefes de família e filhos de chefes de família ricos, com posses, afluentes, o conhecem assim: 'Esse bom lojista tem olhos aguçados e é responsável; ele é capaz de sustentar a sua esposa e filhos e nos pagar de volta no tempo devido.' Assim eles lhe oferecem recursos, dizendo: 'Tendo obtido riqueza com esses recursos, amigo lojista, sustente a sua esposa e filhos e nos pague de volta no tempo devido.' É desse modo que um lojista tem patrocinadores. </p>

<p class=Normal>"Possuindo esses três fatores, um lojista em pouco tempo obtém vasta e abundante riqueza. </p>

<p class=Normal>"Da mesma forma, possuindo três qualidades, um bhikkhu em pouco tempo obtém vasta e abundante qualidades benéficas. Quais três? Aqui, um bhikkhu tem olhos aguçados, é responsável e tem patrocinadores. </p>

<p class=Normal>"E como, bhikkhus, um bhikkhu tem olhos aguçados? Aqui, um bhikkhu compreende como na verdade é: ‘Isto é sofrimento … Esta é a origem do sofrimento … Esta é a cessação do sofrimento … Este é o caminho que conduz à cessação do sofrimento.' É desse modo que um bhikkhu tem olhos aguçados. </p>

<p class=Normal>"E como um bhikkhu é responsável? Aqui, um bhikkhu estimulou a energia para o abandono de qualidades mentais prejudiciais e a obtenção de qualidades mentais benéficas; ele é decidido, firme no seu esforço, não se esquivando da sua responsabilidade de cultivar qualidades mentais benéficas. É desse modo que um bhikkhu é responsável. </p>

<p class=Normal>"E como um bhikkhu tem patrocinadores? Aqui, de tempos em tempos, um bhikkhu procura aqueles bhikkhus que aprenderam muito, que conhecem bem as tradições, que mantêm o Dhamma, a Disciplina e os Códigos [<a href="#N1">1</a>] e os questiona e pergunta da seguinte forma: ‘Como é isto, venerável senhor? Qual o significado disto?’ Esses veneráveis revelam o que não foi revelado, esclarecem o que não está claro ou removem as suas dúvidas acerca das várias coisas que fazem surgir a dúvida. É desse modo que um bhikkhu tem patrocinadores. </p>

<p class=Normal>"Possuindo três qualidades, um bhikkhu em pouco tempo obtém vasta e abundante qualidades benéficas." </p>

