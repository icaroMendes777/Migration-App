<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
todos os contemplativos e brâmanes que no passado exerceram os vários tipos de
poderes supra-humanos, tais como: tendo sido um, eles se tornaram vários; tendo
sido vários, eles se tornaram um; eles apareceram e desapareceram; eles
cruzaram sem nenhum problema uma parede, um cercado, uma montanha ou através do
espaço; eles mergulharam e saíram da terra como se fosse água; eles caminharam
sobre a água sem afundar como se fosse terra; sentados de pernas cruzadas eles
cruzaram o espaço como se fosse um pássaro; com a sua mão eles tocaram e
acariciaram a lua e o sol tão fortes e poderosos; eles exerceram poderes
corporais até mesmo nos distantes mundos de Brahma – todos assim o fizeram
porque eles desenvolveram e cultivaram as quatro bases do poder espiritual. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Todos os
contemplativos e brâmanes que no futuro irão exercer os vários tipos de poderes
supra-humanos, tais como: tendo sido um, eles se tornam vários... eles exercem
poderes corporais até mesmo nos distantes mundos de Brahma – todos assim o
farão porque eles terão desenvolvido e cultivado as quatro bases do poder
espiritual.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Todos os
contemplativos e brâmanes que no presente exercem os vários tipos de poderes
supra-humanos, tais como: tendo sido um, eles se tornam vários... eles exercem
poderes corporais até mesmo nos distantes mundos de Brahma – todos assim o
fazem porque eles desenvolveram e cultivaram as quatro bases do poder
espiritual.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quais
quatro? Neste caso, bhikkhus, um bhikkhu desenvolve a base do poder espiritual
que possui concentração devido ao desejo e às formações volitivas do esforço.
Ele desenvolve a base do poder espiritual que possui concentração devido à
energia e às formações volitivas do esforço. Ele desenvolve a base do poder
espiritual que possui <span style='mso-spacerun:yes'> </span>concentração devido
à mente e às formações volitivas do esforço. Ele desenvolve a base do poder
espiritual que possui concentração devido à investigação e às formações
volitivas do esforço.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
todos os contemplativos e brâmanes que no passado ... no futuro ... no presente
exercem os vários tipos de poderes supra-humanos ... todos assim o fazem porque
eles desenvolveram e cultivaram as quatro bases do poder espiritual.”<o:p></o:p></span></p>

