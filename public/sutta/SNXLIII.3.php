<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em Savatthi.
“Bhikkhus, eu ensinarei para vocês o incondicionado e o caminho que conduz ao
incondicionado. Ouçam e prestem muita atenção àquilo que eu vou dizer.” – “Sim,
venerável senhor,” os bhikkhus responderam. O Abençoado disse o seguinte: <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que,
bhikkhus, é o incondicionado? A destruição da cobiça, a destruição da raiva, a
destruição da delusão: isso é chamado de incondicionado. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é o caminho que conduz ao incondicionado? Concentração com o
pensamento aplicado e sustentado; concentração sem o pensamento aplicado, só o <span
style='mso-spacerun:yes'> </span>sustentado; concentração sem o pensamento
aplicado e sustentado: [<a href="#N1">1</a>] <a name=R1></a>isso é chamado o
caminho que conduz ao incondicionado. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
bhikkhus, eu ensinei para vocês o incondicionado e o caminho que conduz ao
incondicionado. Aquilo que por compaixão um Mestre deveria fazer para os seus
discípulos, desejando o bem-estar deles, isso eu fiz por vocês, bhikkhus. Ali
estão aquelas árvores, aquelas cabanas vazias. Meditem, bhikkhus, não
adiem, ou então vocês se arrependerão mais tarde. Essa é a nossa instrução para
vocês.&quot;<o:p></o:p></span></p>

