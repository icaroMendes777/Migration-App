<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. </span>Assim
ouvi. Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no
Parque de Anathapindika<span lang=PT-BR style='mso-ansi-language:PT-BR'>. </span>Lá
ele se dirigiu aos monges desta forma: “Bhikkhus” – “Venerável Senhor,” eles
responderam. O Abençoado disse o seguinte<span lang=PT-BR style='mso-ansi-language:
PT-BR'>:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.
“Bhikkhus, eu ensinarei para vocês o sumário e a análise ‘Daquele que teve
uma única Noite Excelente.’ </span>Ouçam e prestem muita atenção ao que 
vou dizer” – “Sim, venerável senhor,” os bhikkhus responderam. O Abençoado
disse o seguinte:<span lang=PT-BR style='mso-ansi-language:PT-BR'><o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><a name=T3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>3. “Que ninguém reviva o passado <br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>ou no
futuro coloque as suas esperanças; [<a href="#N1">1</a>]<br>

<a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>pois o passado foi deixado para trás<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>e o futuro
ainda não foi alcançado.<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>Ao invés
disso, que ele veja com insight<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>cada estado
surgido no presente;[<a href="#N2">2</a>]<br>

<a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>que ele compreenda isso e tenha certeza disso,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>invencível,
inabalável.[<a href="#N3">3</a>]<br>

<a name=R3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Hoje o esforço tem que ser feito;<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>amanhã a
Morte poderá vir, quem sabe?<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>Não há
barganha com a Mortalidade<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>que a
mantenha, com a sua horda, fora disso,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>mas aquele
que permanece assim ardente,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>decidido,
durante o dia, durante a noite - <br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>é ele,
disse o Sábio em Paz; [<a href="#N4">4</a>]<br>

<a name=R4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>que teve uma única noite excelente. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. “Como,
bhikkhus, uma pessoa revive o passado? Pensando, ‘Eu tinha tal forma material no
passado,’ ela se delicia com isso. [<a href="#N5">5</a>] <a name=R5></a>Pensando
‘Eu tinha tal sensação no passado,’ ... ‘Eu tinha tal percepção no passado,’
... ‘Eu tinha tais formações volitivas no passado,’ ... ‘Eu tinha tal consciência no
passado,’ ela se delicia com isso. Assim é como uma pessoa revive o passado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. “E como,
bhikkhus, uma pessoa não revive o passado? Pensando, ‘Eu tinha tal forma
material no passado,’ ela não se delicia com isso. [<a href="#N6">6</a>] <a
name=R6></a>Pensando ‘Eu tinha tal sensação no passado,’ ... ‘Eu tinha tal
percepção no passado,’ ... ‘Eu tinha tais formações volitivas no passado,’ ... ‘Eu tinha
tal consciência no passado,’ ela não se delicia com isso. Assim é como uma
pessoa não revive o passado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. “E como,
bhikkhus, uma pessoa coloca as suas esperanças no futuro? Pensando, ‘Que eu
tenha tal forma material no futuro,’ [<a href="#N7">7</a>] <a name=R7></a>ela
se delicia com isso. Pensando, ‘Que eu tenha tal sensação no futuro,’ ...<span
style="mso-spacerun: yes">  </span>‘Que eu tenha tal percepção no futuro,’ ... ‘Que
eu tenha tais formações volitivas no futuro,’ ...<span style="mso-spacerun: yes"> 
</span>‘Que eu tenha tal consciência,’ ela se delicia com isso. Assim é como
uma pessoa coloca as suas esperanças no futuro.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. “E como,
bhikkhus, uma pessoa não coloca as suas esperanças no futuro? Pensando, ‘Que eu
tenha tal forma material no futuro,’ ela não se delicia com isso. Pensando,
‘Que eu tenha tal sensação no futuro,’ ...<span style="mso-spacerun: yes"> 
</span>‘Que eu tenha tal percepção no futuro,’ ... ‘Que eu tenha tais formações volitivas
no futuro,’ ...<span style="mso-spacerun: yes">  </span>‘Que eu tenha tal
consciência,’ ela não se delicia com isso. Assim é como uma pessoa não coloca
as suas esperanças no futuro.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. “E como,
bhikkhus, uma pessoa é derrotada em relação aos estados que tenham surgido no
presente? [<a href="#N8">8</a>] </span><a name=R8></a>Aqui bhikkhus, uma pessoa
comum sem instrução que não respeita os nobres, que não é proficiente nem
treinada no Dhamma deles, que não respeita os homens verdadeiros, que não é
proficiente nem treinada no Dhamma deles<span lang=PT-BR style='mso-ansi-language:
PT-BR'>, considera a forma material como o eu; ou o eu como possuído de forma material;
ou a forma material como estando no eu; ou o eu como estando na forma material.
Ela considera a sensação como o eu ... a percepção como o eu ... as formações volitivas
como o eu ... considera a consciência como o eu; ou o eu como possuído de
consciência; ou a consciência como estando no eu; ou o eu como estando na consciência.
Assim é como uma pessoa é derrotada em relação aos estados que tenham surgido
no presente.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. “E como,
bhikkhus, uma pessoa é invencível em relação aos estados surgidos no presente? </span>Aqui
bhikkhus um nobre discípulo bem instruído que respeita os nobres, que é
proficiente e treinado no Dhamma deles, que respeita os homens verdadeiros,
que é proficiente e treinado no Dhamma deles<span lang=PT-BR
style='mso-ansi-language:PT-BR'>, não considera a forma material como o eu; ou o eu como
possuído de forma material; ou a forma material como estando no eu; ou o eu
como estando na forma material. Ele não considera a sensação como o eu ... a
percepção como o eu ... as formações volitivas como o eu ... não considera a consciência
como o eu; ou o eu como possuído de consciência; ou a consciência como estando
no eu; ou o eu como estando na consciência. Assim é como uma pessoa é
invencível em relação aos estados que tenham surgido no presente.<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. “Que
ninguém reviva o passado ...<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>que teve
uma única noite excelente.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11.
“Portanto,<span style="mso-spacerun: yes">  </span>foi com referência a isso
que foi dito:<span style="mso-spacerun: yes">  </span>‘Bhikkhus, eu ensinarei
para vocês o sumário e a explicação “Daquele que teve uma única Noite
Excelente.”’”<o:p></o:p></span></p>



<p class=Normal>Isso foi o que disse o Abençoado. Os bhikkhus ficaram
satisfeitos e contentes com as palavras do Abençoado.<span lang=PT-BR
style='mso-ansi-language:PT-BR'><o:p></o:p></span></p>


