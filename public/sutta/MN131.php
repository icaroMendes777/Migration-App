<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[1] Num sentido mais literal as duas primeiras
linhas seriam traduzidas: “Que uma pessoa não retroceda ao passado ou viva na
expectativa do futuro.” O significado será elucidado na parte expositiva do
sutta. [<a href="#R1">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[2] MA: Ele deve contemplar cada estado que surgiu
no momento presente, exatamente no momento em que surgiu, com o insight em
relação à sua impermanência, etc. [<a href="#R2">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[3] <i>Asamhiram asankuppam</i>. MA explica
que isto é dito com o propósito de mostrar o insight e a repetição do insight,
pois o insight é “invencível, inabalável” porque ele não é conquistado ou
abalado pela cobiça e outras contaminações. Em outros textos a expressão
“invencível, inabalável” é usada como descrição de Nibbana (ex: Sn v.1149) ou
da mente libertada (ex: Thag v.649), mas neste caso, parece referir-se a um
estágio no desenvolvimento do insight. A repetição da forma verbal <i>samhirati</i>
nas linhas 8 e 9 sugere que o significado pretendido é a contemplação do
momento presente sem se iludir e adotar a idéia de uma identidade. [<a
href="#R3">Retorna</a>]<o:p></o:p></span></p>

<p class=Normal><a name=N4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[4] O “Sábio em Paz” (<i>santo muni</i>) é o
Buda. [<a href="#R4">Retorna</a>]<o:p></o:p></span></p>

<p class=Normal><a name=N5></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[5] MA: A “pessoa se delicia” ao aplicar sobre
o passado quer seja a cobiça ou uma idéia associada à cobiça. Deve ser observado
que não é a mera recordação do passado, por meio da memória, que causa o
cativeiro, mas reviver as experiências passadas com pensamentos de cobiça. [<a
href="#R5">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[6] Talvez esta sentença, bem como todas as
sentenças equivalentes que vêm em seguida, deveriam ser traduzidas: “A pessoa
não se delicia com aquilo, pensando, ‘Eu tinha tal forma material no passado.’”
Da forma apresentada, a tradução sugere que tais pensamentos surgem, mas sem o
acompanhamento da delícia, enquanto que a alternativa proposta aqui sugere que
os pensamentos não surgem de forma nenhuma. A mesma formulação alternativa pode
ser aplicada aos pensamentos sobre o futuro no verso 7. O texto em Pali permite
ambas interpretações. [<a href="#R6">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N7></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[7] Talvez esta frase deva ser tomada como uma
exclamação: “Que eu possa ter tal forma material no futuro!” [<a href="#R7">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N8></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[8] O verbo aqui e no parágrafo seguinte, <i>samhirati</i>,
se refere à linha “invencível, inabalável” dos versos. MA explica: “Ele é
arrastado pela cobiça e idéias devido à carência de insight.” [<a href="#R8">Retorna</a>]</span><b
