<p class=Normal>"Bhikkhus, há esses dois tipos de grupos. Quais dois? O grupo superficial e o grupo profundo.</p>

<p class=Normal>"E qual é o grupo superficial? O grupo em que os bhikkhus estão inquietos, cheios de si, vaidosos, falantes, divagando em sua conversa, com a atenção plena confusa, sem a clara compreensão, não-concentrados, com a mente dispersa, com as faculdades dos sentidos descontroladas, esse é chamado o grupo superficial.</p>

<p class=Normal>"E qual é o grupo profundo? O grupo em que os bhikkhus não estão inquietos, cheios de si, vaidosos, falantes, divagando em sua conversa, mas com a atenção plena estabelecida, com clara compreensão, concentrados, com a mente unificada e com as faculdades dos sentidos controladas, essa é chamado o grupo profundo.</p>

<p class=Normal>"Esses bhikkhus, são os dois tipos de grupos. Destes dois tipos de grupos, o grupo profundo é superior."</p>



