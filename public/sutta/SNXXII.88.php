<p class=Normal> Assim ouvi. Em certa ocasião o Abençoado estava em Rajagaha no Bambual, no Santuário dos Esquilos. </p>

<p class=Normal> Agora, naquela ocasião o venerável Assaji estava atormentado, sofrendo e gravemente enfermo. Então, ele se dirigiu aos seus assistentes desta forma: "Venham, amigos, vão até o Abençoado, homenageiem-no em meu nome com a sua cabeça aos pés dele e digam: 'Venerável senhor, o venerável Assaji está atormentado, sofrendo e gravemente enfermo; ele homenageia o Abençoado com a cabeça aos seus pés.' Depois diga: 'Seria bom, venerável senhor, se o Abençoado fosse até a residência do venerável Assaji, por compaixão.'"</p>

<p class=Normal> "Sim amigo," os bhikkhus responderam, e foram até onde estava o Abençoado, após homenageá-lo, eles sentaram a um lado e relataram a sua mensagem. O Abençoado consentiu em silêncio. </p>

<p class=Normal> Então o Abençoado se vestiu e tomando a tigela e o manto externo, foi para a residência do venerável Assaji. O venerável Assaji  viu o Abençoado vindo à distância e ficou agitado na cama. O Abençoado disse: "Já basta, Assaji, não fique agitado na cama. Este assento está preparado. Eu sentarei aqui." </p>

<p class=Normal> O Abençoado sentou no assento que havia sido preparado e disse para o venerável Assaji: "Eu espero que você esteja melhorando, espero que você esteja confortável, espero que as suas sensações de dor estejam diminuindo e não aumentando e que a sua diminuição, não o seu aumento, seja evidente." </p>

<p class=Normal> "Venerável senhor, eu não estou melhorando, não me sinto confortável. Minhas sensações de dor estão aumentando, não diminuindo, o seu aumento, não a sua diminuição é evidente." </p>

<p class=Normal> "Eu espero então, Assaji, que você não esteja atormentado pelo remorso e pelo arrependimento." </p>

<p class=Normal> "De fato, venerável senhor, eu sinto muito remorso e muito arrependimento." </p>

<p class=Normal> "Eu espero, Assaji, que você não tenha nada que se repreender com relação à virtude." </p>

<p class=Normal> "Venerável senhor, eu não tenho nada que me repreender com relação à virtude." </p>

<p class=Normal> "Então, Assaji, se você não tem nada que se repreender com relação à virtude, porque você está atormentado pelo remorso e arrependimento?" </p>

<p class=Normal> <a name=R1></a> "Antes, venerável senhor, enquanto estava enfermo permanecia tranquilizando as formações corporais, [<a href="#N1">1</a>] mas agora não obtenho a concentração. Como não obtenho a concentração, eu penso: 'Que eu não decaia!'"</p>

<p class=Normal> "Aqueles contemplativos e brâmanes, Assaji, que consideram a concentração como a essência e identificam a concentração com o ascetismo, ao falharem em obter a concentração, poderão pensar, 'Que nós não decaiamos!'</p>

<p class=Normal> "O que você pensa, Assaji, a forma é permanente ou impermanente “Impermanente, venerável senhor.” – “Aquilo que é impermanente é sofrimento ou felicidade?” – “Sofrimento, venerável senhor.” – “É adequado que aquilo que é impermanente, sofrimento e está sujeito à mudança, seja considerado desta forma: ‘Isso é meu, isso sou eu, isso é o meu eu’?” – “Não venerável senhor.” </p>

<p class=Normal> “Assaji, o que você pensa: A sensação ... percepção ... formações volitivas ... consciência são permanentes ou impermanentes?” – “Impermanentes venerável senhor.” - “Aquilo que é impermanente é sofrimento ou felicidade?” – “Sofrimento, venerável senhor.” – “É adequado que aquilo que é impermanente, sofrimento e está sujeito à mudança, seja considerado desta forma: ‘Isso é meu, isso sou eu, isso é o meu eu’?” – “Não venerável senhor.” </p>

<p class=Normal> Portanto, Assaji, qualquer tipo de forma material, quer seja do passado, do futuro ou do presente ... toda forma material deve ser vista como na verdade ela é, com correta sabedoria, deste modo: ‘Isso não é meu, isso não sou eu, isso não é o meu eu’. Qualquer tipo de sensação ... Qualquer tipo de percepção ... Qualquer tipo de formações volitivas ... Qualquer tipo de consciência ... toda consciência deve ser vista como na verdade ela é, com correta sabedoria, deste modo: ‘Isso não é meu, isso não sou eu, isso não é o meu eu.’ </p>

<p class=Normal> “Vendo desse modo um nobre discípulo bem instruído se desencanta com a forma material, se desencanta com a sensação, se desencanta com a percepção, se desencanta com as formações volitivas, se desencanta com a consciência. </p>

<p class=Normal> “Desencantado, ele se torna desapegado. Através do desapego a sua mente é libertada. Quando ela está libertada surge o conhecimento: ‘Libertada.’ Ele compreende que: ‘O nascimento foi destruído, a vida santa foi vivida, o que deveria ser feito foi feito, não há mais vir a ser a nenhum estado’. </p>

<p class=Normal> “Se ele sentir uma sensação prazerosa, ele compreende: ‘Ela é impermanente’; ele compreende: ‘Não é para se apegar’; ele compreende: ‘Não é para se deleitar.’ Se ele sentir uma sensação dolorosa, ele compreende: ‘Ela é impermanente’; ele compreende: ‘Não é para se apegar’; ele compreende: ‘Não é para se deleitar.’ Se ele sentir uma sensação nem prazerosa, nem dolorosa, ele compreende: ‘Ela é impermanente’; ele compreende: ‘Não é para se apegar’; ele compreende: ‘Não é para se deleitar.’ </p>

<p class=Normal> “Se ele sentir uma sensação prazerosa, ele a sente desapegado; se ele sentir uma sensação dolorosa, ele a sente desapegado; Se ele sentir uma sensação nem prazerosa, nem dolorosa, ele a sente desapegado. </p>

<p class=Normal> “Quando ele sente uma sensação que dá um fim ao corpo, ele compreende: ‘Eu sinto uma sensação que dá um fim ao corpo.’ Quando ele sente uma sensação que dá um fim à vida, ele compreende: ‘Eu sinto uma sensação que dá um fim à vida.’ Ele compreende: ‘Com a dissolução do corpo, depois da morte, tudo que é sentido sem deleite, irá esfriar aqui mesmo, apenas resíduos corporais restarão.’ </p>

<p class=Normal> “Assim como, bhikkhus, uma lamparina queima na dependência do óleo e do pavio, com a exaustão do óleo e do pavio a chama se extingue por falta de combustível, da mesma maneira, bhikkhus, quando um bhikkhu sente uma sensação que dá um fim ao corpo ... que dá um fim à vida ... ele compreende: ‘Eu sinto uma sensação que dá um fim à vida.’ Ele compreende: ‘Com a dissolução do corpo, depois da morte, tudo que é sentido sem deleite, irá esfriar aqui mesmo, apenas resíduos corporais restarão.’” </p>


