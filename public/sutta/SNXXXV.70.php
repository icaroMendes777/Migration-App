<p class=Normal>Então o venerável Upavana foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse: </p> 

<p class=Normal> “Venerável senhor, assim dizem, ‘o Dhamma visível no aqui e agora, o Dhamma visível no aqui e agora.’ De que modo, venerável senhor, o Dhamma é visível no aqui e agora, com efeito imediato, que convida ao exame, que conduz para adiante, para ser experimentado pelos sábios por eles mesmos?” </p>

<p class=Normal> “Aqui, Upavana, tendo visto uma forma com o olho, um bhikkhu experimenta a forma bem como a cobiça pela forma. Ele compreende que a cobiça pela forma existe internamente assim: ‘Há em mim internamente a cobiça pela forma.’ Visto que assim é, Upavana, o Dhamma é visível no aqui e agora, com efeito imediato, que convida ao exame, que conduz para adiante, para ser experimentado pelos sábios por eles mesmos. </p>

<p class=Normal> “Além disso, Upavana, tendo ouvido um som com o ouvido ... tendo conscientizado um objeto mental com a mente, um bhikkhu experimenta o objeto mental bem como a cobiça pelo objeto mental. Ele compreende que a cobiça pelo objeto mental existe internamente assim: ‘Há em mim internamente a cobiça pelo objeto mental.’ Visto que assim é, Upavana, o Dhamma é visível no aqui e agora, com efeito imediato, que convida ao exame, que conduz para adiante, para ser experimentado pelos sábios por eles mesmos. </p>

<p class=Normal> “Mas aqui, Upavana, tendo visto uma forma com o olho, um bhikkhu experimenta a forma sem experimentar a cobiça pela forma. Ele compreende que a cobiça pela forma não existe internamente assim: ‘Não há em mim internamente a cobiça pela forma.’ Visto que assim é, Upavana, o Dhamma é visível no aqui e agora, com efeito imediato, que convida ao exame, que conduz para adiante, para ser experimentado pelos sábios por eles mesmos. </p>

<p class=Normal> “Além disso, Upavana, tendo ouvido um som com o ouvido ... tendo conscientizado um objeto mental com a mente, um bhikkhu experimenta o objeto mental sem experimentar a cobiça pelo objeto mental. Ele compreende que a cobiça pelo objeto mental não existe internamente assim: ‘Não há em mim internamente a cobiça pelo objeto mental.’ Visto que assim é, Upavana, o Dhamma é visível no aqui e agora, com efeito imediato, que convida ao exame, que conduz para adiante, para ser experimentado pelos sábios por eles mesmos.” </p>

