<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, o todo está sujeito ao envelhecimento ... o todo está
sujeito à enfermidade ... o todo está sujeito à morte ... o todo está sujeito à
tristeza ... o todo está sujeito à contaminação ... o todo está sujeito à
destruição ... o todo está sujeito ao desaparecimento ... o todo está sujeito ao
surgimento ... o todo está sujeito à cessação. E qual, bhikkhus, é o todo que
está sujeito ao envelhecimento ... cessação? O olho está sujeito ao
envelhecimento ... cessação. As formas ... A consciência no olho ... O contato
no olho ... Qualquer sensação que surja tendo o contato no olho como condição - experimentada como prazer, dor ou nem prazer, nem dor - isso também está sujeito ao envelhecimento ... cessação.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“ O ouvido ... O nariz
... A língua ... O corpo ... A mente ... Os objetos mentais ... A consciência na mente ... O contato
na mente ... Qualquer sensação que surja tendo o
contato na mente como condição - experimentada como prazer, dor ou nem prazer, nem dor - isso também está sujeito ao envelhecimento
... cessação.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Vendo
desse modo, o nobre discípulo bem instruído se desencanta com o olho, com as
formas, com a consciência no olho, com o contato no olho, com qualquer sensação
que surja tendo o contato no olho como condição ... Ele se desencanta com o
ouvido ... com a mente, com os objetos mentais, com a consciência na mente, com o contato
na mente, com qualquer sensação que surja tendo o contato na
mente como condição - experimentada como prazer, dor ou nem prazer, nem dor. Desencantado ele se torna desapegado. Através do
desapego a sua mente é libertada. Quando ela está libertada surge o
conhecimento: ‘Libertada.’ Ele compreende que: ‘O nascimento foi destruído, a
vida santa foi vivida, o que deveria ser feito foi feito, não há mais vir a ser
a nenhum estado.’”<o:p></o:p></span></p>


