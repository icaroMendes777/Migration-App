<p class=Normal>Então um certo bhikkhu foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse: </p>

<p class=Normal>"É dito, venerável senhor: 'dotado de suportes, dotado de suportes.' De que modo um bhikkhu está dotado de suportes?" </p>

<p class=Normal> (1) "Se suportado pela convicção, um bhikkhu abandona o prejudicial e desenvolve o benéfico, o prejudicial de fato é abandonado por ele. (2) Se, suportado pela noção da vergonha de cometer transgressões ... (3) ... suportado pelo temor de cometer transgressões ... (4) ... suportado pela energia ... (5) ... suportado pela sabedoria, um bhikkhu abandona o prejudicial e desenvolve o benéfico, o prejudicial de fato é abandonado por ele. [<a href="#N1">1</a>]  Um bhikkhu abandonou e abandonou bem o prejudicial quando ele o abandona por ter visto com a nobre sabedoria. [<a href="#N2">2</a>] </p>

<p class=Normal>"Baseando-se nessas cinco coisas, um bhikkhu deve contar com quatro coisas. Quais quatro? [<a href="#N3">3</a>] Aqui, (6) tendo refletido, um bhikkhu usa certas coisas; (7) tendo refletido, um bhikkhu pacientemente suporta certas coisas; (8) tendo refletido, um bhikkhu evita certas coisas; (9) tendo refletido, um bhikkhu remove certas coisas. </p>

<p class=Normal>"Desse modo, bhikkhu, um bhikkhu está dotado de suportes."</p>

