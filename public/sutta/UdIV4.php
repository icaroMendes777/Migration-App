<p class=Normal>Assim ouvi. Em certa ocasião, o Abençoado estava em Rajagaha, no Bambual, no Santuário dos Esquilos. </p>



<p class=Normal>Agora, naquela ocasião os veneráveis Sariputta e Maha Moggallana estavam na Caverna dos Pombos. Então, em uma noite de lua cheia, o venerável Sariputta - com sua cabeça recém raspada - sentado a céu aberto alcançou certo nível de concentração.</p>



<p class=Normal>Naquela ocasião dois yakkhas amigos voavam do norte em direção ao sul. Eles viram o venerável Sariputta - com sua cabeça recém raspada - sentado a céu aberto. Ao vê-lo, o primeiro yakkha disse ao outro, "Amigo, estou inclinado a golpear a cabeça deste contemplativo.”</p>



<p class=Normal>Quando aquilo foi dito, o segundo yakkha advertiu seu amigo: "Basta, amigo, não faça mal a este asceta. Este, amigo, é um contemplativo de grande poder, grande majestade.”</p>



<p class=Normal>Uma segunda vez, o primeiro yakkha disse ao outro, "Amigo, estou inclinado a golpear a cabeça deste contemplativo.”</p>



<p class=Normal>Quando aquilo foi dito, uma segunda vez, o segundo yakkha advertiu seu amigo: "Basta, amigo, não faça mal a este asceta. Este, amigo, é um contemplativo de grande poder, grande majestade.”</p>



<p class=Normal>Uma terceira vez, o primeiro yakkha disse ao outro, "Amigo, estou inclinado a golpear a cabeça deste contemplativo.”</p>



<p class=Normal>Quando aquilo foi dito, uma terceira vez, o segundo yakkha advertiu seu amigo: "Basta, amigo, não faça mal a este asceta. Este, amigo, é um contemplativo de grande poder, grande majestade.”</p>



<p class=Normal>Então, o primeiro yakkha, sem dar importância ao que seu amigo lhe dissera, golpeou a cabeça do venerável ancião Sariputta: um golpe tão forte que derrubaria até um grande elefante, um Naga, ou então faria rachar o pico de uma montanha. Mas bem ali o yakkha - gritando: "Estou em chamas!" - caiu em direção ao Grande Inferno.</p>



<p class=Normal>Agora, o venerável Maha Moggallana - através do olho divino que é purificado e ultrapassa o humano - viu o yakkha golpear a cabeça do venerável Sariputta. Assim que viu aquilo, ele foi até o venerável Sariputta. Ao chegar, ele disse: "Espero que esteja bem, amigo Sariputta. Espero que tenha conforto, que não tenha dor.”</p>



<p class=Normal>"Estou bem, amigo Moggallana. Tenho conforto. Mas tenho de fato uma leve dor de cabeça.”</p>



<p class=Normal>"Que maravilha, amigo Sariputta! Que grande poder, amigo Sariputta! Que grande majestade, amigo Sariputta! Agora mesmo, um yakkha lhe golpeou a cabeça. Um golpe tão forte que derrubaria até um grande elefante, um Naga, ou então faria rachar o pico de uma montanha. E todavia o venerável Sariputta me diz que está bem, tem conforto, mas tem de fato uma leve dor de cabeça.”</p>



<p class=Normal>"Que maravilha, amigo Moggallana! Que grande poder, amigo Moggallana! Que grande majestada, amigo Moggallana! Você pode ver yakkhas! Eu sequer posso ver um duende de barro!”</p>



<p class=Normal>Enquanto isso, com o ouvido divino, que é purificado e ultrapassa o humano, o Abençoado ouviu essa conversa entre estes dois grandes seres. Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p>



	<p class=Normal style='margin-left:.75in'>"Como poderá o sofrimento alcançar<br> 

	a mente daquele que segue firme como uma rocha,<br>

	que não se abala,<br>

	cuja mente foi cultivada e disciplinada,<br>

	que não goza das coisas que causam prazer,<br>

	que não se enraivece pelas coisas que causam raiva?”</p>

