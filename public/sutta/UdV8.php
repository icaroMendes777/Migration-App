<p class=Normal> Em certa ocasião, o Abençoado estava em Rajagaha, no Bambual, no Santuário dos Esquilos. Então, ao amanhecer, no dia de observância do Uposatha, o venerável Ananda se vestiu e tomando a sua tigela e manto externo, foi para Rajagaha para esmolar alimentos. Devadatta viu o venerável Ananda esmolando em Rajagaha e foi até ele e disse: "À partir de hoje, amigo Ananda, eu conduzirei a observância do Uposatha e os assuntos da Sangha separado do Abençoado e separado da Sangha dos bhikkhus."</p>

<p class=Normal>Então o venerável Ananda, depois de haver esmolado alimentos e haver retornado, após a refeição, foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse: "Venerável senhor, ao amanhecer, eu me vesti e tomando a tigela e manto externo, fui para Rajagaha para esmolar alimentos. Devadatta me viu e disse: 'À partir de hoje, amigo Ananda, eu conduzirei a observância do Uposatha e os assuntos da Sangha separado do Abençoado e separado da Sangha dos bhikkhus.' Hoje venerável senhor, Devadatta irá causar um cisma na Sangha; ele irá conduzir a observância do Uposatha e os assuntos da Sangha separado."</p>

<p class=Normal>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p>

<p class=Normal style='margin-left:.75in'> Para o bom fazer o bem, é fácil,<br>
para o mau fazer o bem é difícil;<br>
para o mau fazer o mal é fácil,<br>
para o nobre fazer o mal é difícil.</p>

