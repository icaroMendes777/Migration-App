senhor,” o venerável Ananda respondeu, e tomando o seu pano para sentar seguiu
de perto atrás do Abençoado. <o:p></o:p></span></p>



<p class=Normal><a name="T3_2"></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>3.2. O Abençoado foi até o Santuário de Capala e sentou num assento
