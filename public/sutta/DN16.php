desenvolvida e a sua consciência água é imensurável, e ele faz com que a terra
se sacuda e se agite, e trema violentamente. Essa é a segunda razão. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3.15.
“Outra vez, quando um Bodisatva plenamente atento e plenamente consciente,
falece no paraíso de Tusita e descende no ventre da sua mãe, então a terra se
sacode e se agita, e treme violentamente. Essa é a terceira razão.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3.16.
“Outra vez, quando um Bodisatva plenamente atento e plenamente consciente,
emerge do ventre da sua mãe, então a terra se sacode e se agita, e treme
violentamente. Essa é a quarta razão.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3.17.
“Outra vez, quando o Tathagata realiza a perfeita iluminação, então a terra se
