para o parinibbana do Abençoado!’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3.35. “Em
vista disso, eu disse para Mara: ‘Eu não realizarei o parinibbana, Senhor do
Mal, até que eu tenha discípulos bhikkhus que sejam sábios, ... (igual ao verso
3.7) … discípulas bhikkhunis...<span style='mso-spacerun:yes'>  </span>até que
eu tenha discípulos leigos ... até que eu tenha discípulas leigas, que sejam sábias ... e que sejam capazes de ensinar o Dhamma que é
eficaz. Eu não realizarei o parinibbana, Senhor do Mal, até que esta minha vida
santa tenha se tornado bem sucedida e próspera, extensa, popular, expandida, bem
proclamada entre os devas e humanos.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3.36. “E
justamente agora, hoje, Ananda, no santuário de Capala, Mara veio até a mim,
ficou em pé a um lado e disse: ‘Venerável senhor, que o Abençoado agora realize
