compaixão pelo mundo, pelo bem, bem-estar e felicidade de devas e humanos.” Até
esse ponto a mente dele estava obcecada por Mara.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3.5. Uma
segunda vez... Uma terceira vez... (Igual ao verso 3.4).<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3.6. Então,
o Abençoado se dirigiu ao venerável Ananda: “Você pode ir, Ananda, quando lhe
