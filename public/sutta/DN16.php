<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3.18. “Outra
vez, quando o Tathagata coloca em movimento a Roda do Dhamma, então a terra se
sacode e se agita, e treme violentamente. Essa é a sexta razão.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3.19.
“Outra vez, quando o Tathagata, plenamente atento e plenamente consciente,
