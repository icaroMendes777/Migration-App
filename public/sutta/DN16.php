<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3.45.
“Certa vez eu estava em Vesali no Santuário Udena ...<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3.46.
