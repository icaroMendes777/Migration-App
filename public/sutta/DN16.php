<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Você tem
fé, Ananda?” - “Sim, venerável senhor.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
Ananda, o erro foi seu, você falhou, pois tendo sido dada uma sugestão tão
