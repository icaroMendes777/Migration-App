incomoda o Tathagata com esse pedido três vezes?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3.40. “Mas
venerável senhor, eu ouvi isso da própria boca do Abençoado, eu compreendi isso
