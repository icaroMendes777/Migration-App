<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3.26.
“Percebendo a forma no interior, ele vê a forma no exterior, imensurável,
bonita e feia; ao transcendê-la, ele percebe assim: ‘Eu sei, eu vejo.’ Essa é a
segunda base para a transcendência.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3.27. “Não
