senhor, a Sangha dos bhikkhus está reunida. Agora é o momento, Abençoado, faça
como julgar adequado.”<o:p></o:p></span></p>



<p class=Normal><a name="T3_50"></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>3.50. Então, o Abençoado entrou no salão de assembléias e sentou num
