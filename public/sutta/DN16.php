é Vesali, Ananda. Encantador é o santuário Udena, encantador é o santuário
Gotamaka, encantador é o santuário Sattamba, encantador é o santuário
Bahuputta, encantador é o santuário Sarandada, encantador é o santuário Capala.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3.3.
“Qualquer um, Ananda, que tenha desenvolvido e cultivado as quatro bases do
poder espiritual, que tenha feito delas o seu veículo, a sua base, que as tenha
estabilizado, que nelas tenha se exercitado e que as tenha aperfeiçoado,
poderia, se ele assim <span class=GramE>desejasse,</span> seguir vivendo
durante aquele éon ou o tempo que restasse de um éon. [<a href="#N15">15</a>] <a
name=R15></a>O Tathagata, Ananda, desenvolveu e cultivou as quatro bases do
poder espiritual, fez delas o seu veículo, a sua base, estabilizou-as, se
exercitou nelas e aperfeiçoou-as. Se ele assim desejasse, o Tathagata poderia
seguir vivendo durante este éon ou o tempo que resta do éon.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3.4. Mas,
