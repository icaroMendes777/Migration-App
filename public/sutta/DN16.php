<span lang=PT-BR style='mso-ansi-language:
PT-BR'>aquele, cuja visão deu um fim ao sofrimento, realizou nibbana.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4.4. Então,
o Abençoado, enquanto estava em Bhandagama, freqüentemente aconselhava os
