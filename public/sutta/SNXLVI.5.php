<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, dizem, ‘fatores da iluminação, fatores da iluminação.’ Com qual sentido
eles são chamados de fatores da iluminação?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eles
conduzem à iluminação, bhikkhu, portanto eles são chamados de fatores da
iluminação. Aqui, bhikkhu, alguém desenvolve o fator da iluminação da atenção
plena, que tem como base o afastamento, o desapego e a cessação, que amadurece
no abandono [<a href="#N1">1</a>] <a name=R1></a>... Alguém desenvolve o fator
da iluminação da equanimidade, que tem como base o afastamento, o desapego e a
cessação, que amadurece no abandono. Enquanto ele desenvolve esses sete fatores
da iluminação, a mente dele é libertada da impureza do prazer sensual, da
impureza do ser/existir, da impureza da ignorância. Quando ela está libertada
surge o conhecimento: ‘Libertada.’ Ele compreende que: ‘O nascimento foi
destruído, a vida santa foi vivida, o que deveria ser feito foi feito, não há
mais vir a ser a nenhum estado.’ Eles conduzem à iluminação, bhikkhu, portanto
eles são chamados de fatores da iluminação.”<o:p></o:p></span></p>




