<p class=Normal> Em Savatthi. Então um certo bhikkhu foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse:</p>

<p class=Normal> "Venerável senhor, como alguém deve compreender, como alguém deve ver, para que as impurezas sejam abandonadas? ... para que as impurezas sejam desenraizadas? ... para que as tendências subjacentes sejam abandonadas? ... para que as tendências subjacentes sejam desenraizadas?"</p>

<p class=Normal> "Bhikkhu, quando alguém compreende e vê o olho como impermanente ... como não-eu, as impurezas são abandonadas ...  as impurezas são desenraizadas ... as tendências subjacentes são abandonadas ... as tendências subjacentes são desenraizadas. </p>

<p class=Normal>"Bhikkhu, quando alguém compreende as formas como impermanentes ... como não-eu, as impurezas são abandonadas ...  as impurezas são desenraizadas ... as tendências subjacentes são abandonadas ... as tendências subjacentes são desenraizadas.</p>

<p class=Normal>"Bhikkhu, quando alguém compreende e vê como impermanente ... como não-eu qualquer sensação que surja tendo o contato no olho ... na mente como condição - quer seja prazerosa, dolorosa ou nem prazerosa nem dolorosa - as impurezas são abandonadas ...  as impurezas são desenraizadas ... as tendências subjacentes são abandonadas ... as tendências subjacentes são desenraizadas. </p>

<p class=Normal>"Bhikkhu, quando alguém compreende e vê assim as impurezas são abandonadas ...  as impurezas são desenraizadas ... as tendências subjacentes são abandonadas ... as tendências subjacentes são desenraizadas."</p>

