<p class=Normal>Assim ouvi. Em certa ocasião o Ven. Ananda estava em Kosambi
no Parque de Ghosita. Lá ele se dirigiu aos monges desta forma: “Amigos,
bhikkhus.”</p>


<p class=Normal>“Sim amigo, os bhikkhus responderam.</p>

<p class=Normal>O Ven. Ananda disse: “É maravilhoso, amigos, é admirável, como o Abençoado
que sabe e vê, um arahant, perfeitamente iluminado, realizou e identificou que há a
possibilidade de purificação dos seres, para superar a tristeza e a lamentação,
para o desaparecimento da dor e da angústia, para alcançar o caminho
verdadeiro, para a realização de Nibbana, onde o olho e as formas estão presentes, no entanto não há a experiência dessas bases, onde o ouvido e os
sons … onde o nariz e os aromas … onde a língua e os sabores … onde o
corpo e os tangíveis estão presentes, no entanto não há a experiência dessas bases.“ </p>

<p class=Normal>Quando isso foi dito o Ven. Udayin disse para o Ven. Ananda: “Quando não há a experiência dessas bases, alguém está perceptivo ou não
perceptivo?“ </p>

<p class=Normal>Ananda: “Quando alguém está perceptivo não há a experiência dessas bases.“ </p>

<p class=Normal>Udayin: “Mas alguém  está perceptivo do que, quando não há a experiência dessas bases?“ </p>

<p class=Normal>Ananda: “Há o caso em que com a completa superação das percepções da forma, com o desaparecimento das
percepções do contato sensorial, sem dar atenção às percepções da diversidade,
consciente de que o ‘espaço é infinito,’ ele entra e permanece na base do
espaço infinito. </span>Quando alguém está assim perceptivo não há a experiência dessa base.</p>

<p class=Normal>“Além disso, <span lang=PT-BR style='mso-ansi-language:PT-BR'>com a completa
superação da base do espaço infinito, consciente de que a ‘consciência é
infinita,’ ele entra e permanece na base da consciência infinita. </span>Quando alguém está assim perceptivo não há a experiência dessa base. </p>

<p class=Normal>“Além disso, <span lang=PT-BR style='mso-ansi-language:PT-BR'>com a completa
superação da base da consciência infinita, consciente de que ‘não há nada,’ ele
entra e permanece na base do nada. </span>Quando alguém está assim perceptivo não há a experiência dessa base. </p>

<p class=Normal>“Certa vez, amigo, quando eu estava em Saketa na Reserva do Gamo na Floresta
Negra, a bhikkhuni Jatila Bhagika foi até onde eu estava e ao
chegar me cumprimentou e ficando em pé a um lado ela disse: ‘A concentração
que não se inclina para frente, tampouco se volta para trás, que não é refreada ou contida pela supressão [das contaminações] – estável como resultado da libertação,
satisfeita como resultado da estabilidade, sem estar agitada como resultado da
satisfação: É dito pelo Abençoado ser essa concentração fruto do quê?’</p>

<p class=Normal>Eu respondi, ‘... , é dito pelo Abençoado ser essa concentração o fruto do
conhecimento [o conhecimento da Iluminação].’ Essa é outra forma de ser
perceptivo sem haver a experiência dessa base.</p>

