<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em certa
ocasião, o Abençoado estava<span style='mso-spacerun:yes'>  </span>entre os
Vajjians, em Ukkacela, às margens do rio Gânges, junto com uma grande Sangha de
bhikkhus, não muito tempo depois que Sariputta e Maha Moggallana haviam
realizado o parinibbana. Agora, naquela ocasião, o Abençoado estava sentado
ao ar livre, circundado pelos bhikkhus.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, o Abençoado tendo
inspecionado o grupo silencioso de bhikkhus, se dirigiu a eles dizendo:<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus, esta assembléia
aparenta estar vazia agora que Sariputta e Maha Moggallana realizaram o parinibbana. Esta assembléia não estava vazia antes, e eu não me preocupava com
Sariputta e Maha Moggallana onde quer que estivessem.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Os Arahants, Perfeitamente
Iluminados, que surgiram no passado também tiveram um tal par de discípulos
excelentes, como eu tive Sariputta e Maha Moggallana. Os Arahants,
Perfeitamente Iluminados, que surgirão no futuro também irão ter um tal par de
discípulos excelentes, como eu tive Sariputta e Maha Moggallana.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“É maravilhoso, bhikkhus,
com respeito a esses discípulos, é admirável, bhikkhus, com respeito a esses
discípulos, que eles irão agir de acordo com os ensinamentos do Mestre e
obedecer as suas admoestações, que eles serão estimados pelas quatro
Assembléias, que eles serão reverenciados e respeitados por elas. É maravilhoso,
bhikkhus, com respeito ao Tathagata, é admirável, bhikkhus, com respeito ao
Tathagata que, quando um par de discípulos como esse tenha realizado o parinibbana,
 não haja tristeza ou lamentação no Tathagata.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR>“Bhikkhus, eu já não lhe disse antes: todas
aquelas coisas que para nós são queridas, estimadas e prazerosas estão sujeitas
à mudança, separação e alteração? Então, como poderia ser possível que tudo
aquilo que nasce, veio a ser, condicionado, que está sujeito à dissolução, não
desapareça – isso é impossível. É como se os maiores galhos de uma grande
árvore possuidora de cerne caíssem: da mesma forma, bhikkhus, na grande Sangha
dos Bhikkhus possuidora de cerne, Sariputta e Moggalana realizaram o parinibbana. Como, bhikkhus, poderia ser possível: ‘Que aquilo que nasceu, veio a ser,
condicionado, que está sujeito à dissolução, não desapareça? Isso é impossível.’<span
style='mso-spacerun:yes'>  </span></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Portanto,
bhikkhus, sejam ilhas para vocês mesmos, refúgios para vocês mesmos, buscando
nenhum refúgio externo; com o Dhamma como a sua ilha, o Dhamma como o seu
refúgio, buscando nenhum outro refúgio. E como, bhikkhus, um bhikkhu é uma ilha
para ele mesmo, um refúgio para ele mesmo, buscando nenhum refúgio externo; com
o Dhamma como a sua ilha, o Dhamma como o seu refúgio, buscando nenhum outro
refúgio? Neste caso, bhikkhus, um bhikkhu permanece contemplando o corpo como
um corpo, ardente, plenamente consciente e com atenção plena, tendo colocado de
lado a cobiça e o desprazer pelo mundo. Ele permanece contemplando as sensações
como sensações ... mente como mente ... objetos mentais como objetos mentais,
ardente, plenamente consciente e com atenção plena, tendo colocado de lado a cobiça
e o desprazer pelo mundo.</span><span lang=PT-BR style='mso-bidi-font-size:
10.0pt;mso-ansi-language:PT-BR'><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Esses bhikkhus que agora
ou depois que eu me for, permanecerem como uma ilha para eles mesmos, como um
refúgio para eles mesmos, sem nada mais como refúgio, com o Dhamma como uma
ilha, o Dhamma como refúgio, sem nada mais como refúgio, eles serão para mim os
bhikkhus mais eminentes dentre aqueles que têm interesse em aprender.&quot; <o:p></o:p></span></p>

