<p class=Normal> Assim ouvi. Certa ocasião o Abençoado estava em Vesali na Grande Floresta no Salão com um pico na cumeeira. Então um certo bhikkhu Vajji foi até o Abençoado e depois de cumprimentá-lo disse: </p>

<p class=Normal> "Venerável senhor, cada quinzena mais de cento e cinquenta regras de treinamento são recitadas. Eu não consigo adotá-las para treinamento." </p>

<p class=Normal> "Bhikkhu, você é capaz de treinar nos três treinamentos: o treinamento na virtude superior, o treinamento na mente superior e o treinamento na sabedoria superior?" </p>

<p class=Normal> "Sou capaz, venerável senhor." </p>

<p class=Normal> "Bhikkhu, portanto treine adotando os três treinamentos: o treinamento na virtude superior, o treinamento na mente superior e o treinamento na sabedoria superior. Treinando neles, você abandonará a cobiça, a raiva e a delusão. Com o abandono da cobiça, da raiva e da delusão você não fará nada inábil e não fará uso de nada impróprio." </p>

<p class=Normal> Então, pouco tempo depois, aquele bhikkhu adotou o treinamento na virtude superior, o treinamento na mente superior e o treinamento na sabedoria superior. Na medida em que neles treinava  ele abandonou a cobiça, a raiva e a delusão. Com o abandono da cobiça, da raiva e da delusão ele não fez nada inábil e não fez uso de nada impróprio.</p>

