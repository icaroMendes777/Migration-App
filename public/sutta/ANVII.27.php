<p class=Normal> "Bhikkhus, eu lhes ensinarei os sete princípios do não-declínio. Ouçam e prestem muita atenção àquilo que eu vou dizer." </p>

<p class=Normal>"Sim, venerável senhor," aqueles bhikkhus responderam. O Abençoado disse o seguinte: </p>

<p class=Normal>"Quais, bhikkhus, são os sete princípios do não-declínio? </p>

<p class=Normal> (1) "Enquanto os bhikkhus desenvolverem a percepção da impermanência, eles apenas devem esperar desenvolvimento, não declínio. (2) Enquanto os bhikkhus desenvolverem a percepção de não-eu ... (3) ... desenvolverem a percepção das coisas repulsivas no corpo ... (4) ... desenvolverem a percepção do perigo ... (5) ... desenvolverem a percepção do abandono ... (6) ... desenvolverem a percepção do desapego ... (7) ... desenvolverem a percepção da cessação, eles apenas devem esperar desenvolvimento, não declínio. [<a href="#N1">1</a>]</p>

<p class=Normal>"Bhikkhus, enquanto esses sete princípios do não-declínio persistirem entre os bhikkhus, e os bhikkhus forem vistos estarem neles estabelecidos, eles apenas devem esperar desenvolvimento, não declínio." </p>


