<p class=Normal> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: "Bhikkhus, há três tipos de filhos que podem ser encontrados no mundo. Quais três? O tipo superior, o tipo semelhante, e o tipo inferior.</p>

<p class=Normal> "Agora, bhikkhus, qual é  tipo de filho superior? Nesse caso um filho tem uma mãe e um pai que não buscaram refúgio no Buda, no Dhamma, e na Sangha; que não se abstêm de tirar a vida de outros seres, de tomar o que não é dado, da conduta imprópria em relação aos prazeres sensuais, da linguagem mentirosa e do vinho, álcool e outros embriagantes que causam a negligência; que não são virtuosos e têm conduta ruim. Mas o filho buscou refúgio no Buda, no Dhamma, e na Sangha; se abstém de tirar a vida de outros seres, de tomar o que não é dado, da conduta imprópria em relação aos prazeres sensuais, da linguagem mentirosa e do vinho, álcool e outros embriagantes que causam a negligência; é virtuoso e tem conduta boa.</p>

<p class=Normal> "Agora, bhikkhus, qual é  tipo de filho semelhante? Nesse caso um filho tem uma mãe e um pai que buscaram refúgio no Buda, no Dhamma, e na Sangha; que se abstêm de tirar a vida de outros seres, de tomar o que não é dado, da conduta imprópria em relação aos prazeres sensuais, da linguagem mentirosa e do vinho, álcool e outros embriagantes que causam a negligência; que são virtuosos e têm conduta boa. E o filho buscou refúgio no Buda, no Dhamma, e na Sangha; se abstém de tirar a vida de outros seres, de tomar o que não é dado, da conduta imprópria em relação aos prazeres sensuais, da linguagem mentirosa e do vinho, álcool e outros embriagantes que causam a negligência; é virtuoso e tem conduta boa.</p>

<p class=Normal> "Agora, bhikkhus, qual é  tipo de filho inferior? Nesse caso um filho tem uma mãe e um pai que buscaram refúgio no Buda, no Dhamma, e na Sangha; que se abstêm de tirar a vida de outros seres, de tomar o que não é dado, da conduta imprópria em relação aos prazeres sensuais, da linguagem mentirosa e do vinho, álcool e outros embriagantes que causam a negligência; que são virtuosos e têm conduta boa. Mas o filho não buscou refúgio no Buda, no Dhamma, e na Sangha; não se abstém de tirar a vida de outros seres, de tomar o que não é dado, da conduta imprópria em relação aos prazeres sensuais, da linguagem mentirosa e do vinho, álcool e outros embriagantes que causam a negligência; não é virtuoso e tem conduta ruim.</p>

<p class=Normal> "Esses bhikkhus, são os três tipos de filhos que podem ser encontrados no mundo."</p>

<p class=Normal style='margin-left:.75in'> Os sábios desejam um filho <br>
superior ou semelhante. <br>
Eles não desejam um filho inferior, <br>
que desonra a família.</p>

<p class=Normal style='margin-left:.75in'> Pois esse tipo de filhos no mundo<br>
que são discípulos leigos devotos, <br>
distinguindo-se em fé e virtude, <br>
generosos, sem egoísmo, <br>
brilham nas assembléias, <br>
tal como a lua livre das nuvens.</p>


