<p class=Normal> Assim ouvi. Certa ocasião o Abençoado estava em Rajagaha na Montanha do Pico do Abutre. Então o errante Sajjha foi até o Abençoado e eles se cumprimentaram, quando a conversa amigável e cortês havia terminado ele sentou a um lado e disse para o Abençoado: </p>

<p class=Normal>"Venerável senhor, certa ocasião o Abençoado estava aqui em Rajagaha. Naquela ocasião, na presença do Abençoado, eu ouvi e aprendi isto: 'Sajjha, um bhikkhu que é um Arahant, com as impurezas destruídas, que viveu a vida santa, fez o que devia ser feito, depôs o fardo, alcançou o verdadeiro objetivo, destruiu os grilhões da existência e está completamente libertado através do conhecimento supremo, é incapaz de transgredir em nove casos. Ele é incapaz de intencionalmente privar da vida um ser vivo; ele é incapaz de tomar aquilo que não tenha sido dado; ele é incapaz de praticar o ato sexual; ele é incapaz de deliberadamente dizer uma mentira; ele é incapaz de armazenar coisas de modo a desfrutar de prazeres dos sentidos tais como ele fazia quando era leigo; ele é incapaz de agir incorretamente por conta do desejo; ele é incapaz de agir incorretamente por conta da raiva; ele é incapaz de agir incorretamente por conta da delusão; ele é incapaz de agir incorretamente por conta do medo. Venerável senhor, eu ouvi corretamente, prestei atenção corretamente, entendi corretamente, lembrei corretamente?" </p>

<p class=Normal>"Sim, Sajjha, você ouviu corretamente, prestou atenção corretamente, entendeu corretamente, lembrou corretamente, e também agora eu digo isso: (1) Ele é incapaz de intencionalmente privar da vida um ser vivo; (2) ele é incapaz de tomar aquilo que não tenha sido dado; (3) ele é incapaz de praticar o ato sexual; (4) ele é incapaz de deliberadamente dizer uma mentira; (5) ele é incapaz de armazenar coisas de modo a desfrutar de prazeres dos sentidos tais como ele fazia quando era leigo; (6) ele é incapaz de agir incorretamente por conta do desejo; (7) ele é incapaz de agir incorretamente por conta da raiva; (8) ele é incapaz de agir incorretamente por conta da delusão; (9) ele é incapaz de agir incorretamente por conta do medo.</p>

<p class=Normal>"Tanto antes como agora eu lhe digo que um bhikkhu que é um Arahant, com as impurezas destruídas, que viveu a vida santa, fez o que devia ser feito, depôs o fardo, alcançou o verdadeiro objetivo, destruiu os grilhões da existência e está completamente libertado através do conhecimento supremo, é incapaz de transgredir nesses nove casos." </p>

