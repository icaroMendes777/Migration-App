<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. [</span><a href="#N1"><span lang=PT-BR style='mso-ansi-language:PT-BR'>1</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R1></a>Em certa ocasião o
Abençoado estava em Savatthi no Bosque de Jeta, no Parque de Anathapindika.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. Então,
ao amanhecer, o Abençoado se vestiu e tomando a tigela e o manto externo, foi
para Savatthi para esmolar alimentos. O venerável Rahula também se vestiu e tomando
a tigela e o manto externo, seguiu de perto atrás do Abençoado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. Nessa
ocasião, o Abençoado olhou para trás e se dirigiu ao venerável Rahula assim: [<a
href="#N2">2</a>] <a name=R2></a>“Rahula, qualquer tipo de forma material, quer
seja do passado, futuro ou presente, interna ou externa, grosseira ou sutil,
inferior ou superior, próxima ou distante, toda forma material deve ser vista
como na verdade ela é, com a correta sabedoria, assim: ‘Isso não é meu, isso
não sou eu, isso não é o meu eu’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Apenas a forma
material, Abençoado? Apenas a forma material, Sublime?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“A forma
material, Rahula, e a sensação, a percepção, as formações volitivas e a consciência.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. Então o
venerável Rahula pensou o seguinte: “Quem iria para a cidade esmolar alimentos
hoje depois de ter sido pessoalmente admoestado pelo Abençoado?” Assim, ele
regressou e sentou-se à sombra de uma árvore, com as pernas cruzadas, mantendo o
corpo ereto e estabelecendo a plena atenção à sua frente.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. O venerável
Sariputta o viu ali sentado e se dirigiu a ele da seguinte forma: “Rahula,
desenvolva a atenção plena na respiração. Quando a atenção plena na respiração
é desenvolvida e cultivada, ela traz grandes frutos e grandes benefícios.” [<a
href="#N3">3</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>6. Então, ao anoitecer, o venerável Rahula se levantou da meditação e
foi até o Abençoado. Depois de cumprimentá-lo, ele sentou a um lado e perguntou
ao Abençoado:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7.
“Venerável senhor,<span style='mso-spacerun:yes'>  </span>como a atenção plena
na respiração é desenvolvida e cultivada para que traga grandes frutos e
grandes benefícios?” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(OS QUATRO
GRANDES ELEMENTOS)</b><o:p></o:p></span></p>



<p class=Normal><a name=T8></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>8. “O que, Rahula, [<a href="#N4">4</a>] <a name=R4></a> é o elemento
terra? O elemento terra pode ser interno ou externo. O que é o elemento terra
interno? Qualquer coisa interna que pertença à pessoa, que seja sólida,
solidificada e pela qual exista apego, isto é, cabelos, pêlos do corpo, unhas,
dentes, pele, carne, tendões, ossos, medula, rins, coração, fígado, diafragma,
baço, pulmões, intestino grosso, intestino delgado, conteúdo do estômago, fezes
ou qualquer outra coisa interna que pertença à pessoa, que seja sólida,
solidificada e pela qual exista apego: é chamada de elemento terra interno.
Agora, tanto o elemento terra interno como o elemento terra externo são simplesmente
elementos terra. E isso deve ser visto como na verdade é, com correta
sabedoria: ‘Isso não é meu, isso não sou eu, isso não é o meu eu.’ Quando a
pessoa vê dessa forma, como na verdade é, com correta sabedoria, a pessoa fica
desencantada com o elemento terra e faz com que a mente fique desapegada em
relação ao elemento terra.<span style='color:red'><o:p></o:p></span></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. “O que,
Rahula, é o elemento água? O elemento água pode ser interno ou externo. Qual é
o elemento água interno ? Qualquer coisa interna que pertença à pessoa, que
seja líquida, aquosa e pela qual exista apego, isto é, bílis, fleuma, pus,
sangue, suor, gordura, lágrimas, óleo, saliva, muco, líquido sinovial, urina ou
qualquer outra coisa interna na pessoa, que seja líquida, aquosa e pela qual
exista apego: é chamada de elemento água interno. Agora, tanto o elemento água
interno como o elemento água externo são simplesmente elementos água. E isso
deve ser visto como na verdade é, com correta sabedoria: ‘Isso não é meu, isso
não sou eu, isso não é o meu eu.’ Quando a pessoa vê dessa forma, como na
verdade é, com correta sabedoria, a pessoa fica desencantada com o elemento
água e faz com que a mente fique desapegada em relação ao elemento água.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. “O que,
Rahula, é o elemento fogo? O elemento fogo pode ser interno ou externo. Qual é
o elemento fogo interno ? Qualquer coisa interna que pertença à pessoa, que
seja fogo, ardente e pela qual exista apego, isto é, aquilo pelo qual a pessoa
é aquecida, envelhece e é consumida, aquilo pelo qual o que é comido, bebido,
consumido e saboreado é digerido de maneira adequada ou qualquer outra coisa
interna na pessoa, que seja fogo, ardente e pela qual exista apego: é chamada
de elemento fogo interno. Agora, tanto o elemento fogo interno como o elemento
fogo externo são simplesmente elementos fogo. E isso deve ser visto como na
verdade é, com correta sabedoria: ‘Isso não é meu, isso não sou eu, isso não é
o meu eu.’ Quando a pessoa vê dessa forma, como na verdade é, com correta
sabedoria, a pessoa fica desencantada com o elemento fogo e faz com que a mente
fique desapegada em relação ao elemento fogo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. “O que,
Rahula, é o elemento ar? O elemento ar pode ser interno ou externo. Qual é o
elemento ar interno? Qualquer coisa interna, que pertença à pessoa, que seja
ar, arejada e pela qual exista apego, isto é, ventos que sobem, ventos que
descem, ventos no estômago, ventos nos intestinos, ventos que percorrem o
corpo, a inspiração e a expiração ou qualquer outra coisa interna na pessoa que
seja ar, arejada e pela qual exista apego:<span style='mso-spacerun:yes'> 
</span>é chamada de elemento ar interno. Agora, tanto o elemento ar interno
como o elemento ar externo são simplesmente elementos ar. E isso deve ser visto
como na verdade é, com correta sabedoria: ‘Isso não é meu, isso não sou eu,
isso não é o meu eu.’ Quando a pessoa vê dessa forma, como na verdade é, com
correta sabedoria, a pessoa fica desencantada com o elemento ar e faz com que a
mente fique desapegada em relação ao elemento ar.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. “O que,
Rahula, é o elemento espaço? [<a href="#N5">5</a>] <a name=R5></a>O elemento
espaço pode ser interno ou externo. Qual é o elemento espaço interno? Qualquer
coisa interna na pessoa, que seja espaço, espacial e pela qual exista apego,
isto é, os buracos das orelhas, as narinas, a boca e a abertura através da qual
tudo o que é comido, bebido, consumido e saboreado, engolido e agrupado é
excretado por baixo; ou qualquer outra coisa interna na pessoa, que seja
espaço, espacial e pela qual exista apego: é chamada de elemento espaço
interno. Agora, ambos, o elemento espaço interno e o elemento espaço externo
são simplesmente propriedades do espaço. E isso deve ser visto como na verdade
é com correta sabedoria: ‘Isso não é meu, isso não sou eu, isso não é o meu
eu.’ Quando a pessoa vê dessa forma como na verdade é com correta sabedoria, a
pessoa fica desencantada com o elemento espaço e faz com que a mente fique
desapegada em relação ao elemento espaço.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13.
“Rahula, desenvolva uma meditação que seja como a terra; pois quando você
desenvolve uma meditação que é como a terra, os contatos que já surgiram, agradáveis
e desagradáveis, não invadirão e permanecerão na sua mente. [<a href="#N6">6</a>]
<a name=R6></a>Da mesma forma que as pessoas jogam na terra coisas limpas e
coisas sujas, excremento, urina, saliva, pus e sangue, e a terra não fica
horrorizada, repelida e enojada por isso, assim também, Rahula, desenvolva uma
meditação que seja como a terra; pois quando você desenvolve uma meditação que
é como a terra, os contatos que já surgiram, agradáveis e desagradáveis, não
invadirão e permanecerão na sua mente.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14.
“Rahula, desenvolva uma meditação que seja como a água; pois quando você
desenvolve uma meditação que é como a água, os contatos que já surgiram,
agradáveis e desagradáveis, não invadirão e permanecerão na sua mente. Da mesma
forma que as pessoas lavam na água coisas limpas e coisas sujas, excremento,
urina, saliva, pus e sangue, e a água não fica horrorizada, repelida e enojada
por isso, assim também, Rahula, desenvolva uma meditação que seja como a água;
pois quando você desenvolve uma meditação que é como a água, os contatos que
já<span style='mso-spacerun:yes'>  </span>surgiram, agradáveis e desagradáveis,
não invadirão e permanecerão na sua mente.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15.
“Rahula, desenvolva uma meditação que seja como o fogo; pois quando você
desenvolve uma meditação que é como o fogo, os contatos que já surgiram,
agradáveis e desagradáveis, não invadirão e permanecerão na sua mente. Da mesma
forma que as pessoas queimam no fogo coisas limpas e coisas sujas, excremento,
urina, saliva, pus e sangue, e o fogo não fica horrorizado, repelido e enojado
por isso, assim também, Rahula, desenvolva uma meditação que seja como o fogo;
pois quando você desenvolve uma meditação que é como o fogo, os contatos que
já<span style='mso-spacerun:yes'>  </span>surgiram, agradáveis e desagradáveis,
não invadirão e permanecerão na sua mente.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16.
“Rahula, desenvolva uma meditação que seja como o ar; pois quando você
desenvolve uma meditação que é como o ar, os contatos que já<span
style='mso-spacerun:yes'>  </span>surgiram, agradáveis e desagradáveis, não
invadirão e permanecerão na sua mente. Da mesma forma que o ar sopra nas coisas
limpas e coisas sujas, excremento, urina, saliva, pus e sangue, e o ar não fica
horrorizado, repelido e enojado por isso, assim também, Rahula, desenvolva uma
meditação que seja como o ar; pois quando você desenvolve uma meditação que é
como o ar, os contatos que já<span style='mso-spacerun:yes'>  </span>surgiram,
agradáveis e desagradáveis, não invadirão e permanecerão na sua mente.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17.
“Rahula, desenvolva uma meditação que seja como o espaço; pois quando você
desenvolve uma meditação que é como o espaço, os contatos que já<span
style='mso-spacerun:yes'>  </span>surgiram, agradáveis e desagradáveis, não
invadirão e permanecerão na sua mente. Da mesma forma que o espaço não está
estabelecido em nenhum lugar, assim também, Rahula, desenvolva uma meditação
que seja como o espaço; pois quando você desenvolve uma meditação que é como o
espaço, os contatos que já<span style='mso-spacerun:yes'>  </span>surgiram,
agradáveis e desagradáveis, não invadirão e permanecerão na sua mente.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18.
“Rahula, desenvolva a meditação do amor bondade; pois quando você desenvolve a
meditação do amor bondade, toda má vontade será abandonada.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>19. “Rahula,
desenvolva a meditação da compaixão; pois quando você desenvolve a meditação da
compaixão, toda crueldade será abandonada.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>20.
“Rahula, desenvolva a meditação da alegria altruísta; pois quando você
desenvolve a meditação da alegria altruísta, todo descontentamento será
abandonado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>21.
“Rahula, desenvolva a meditação da equanimidade; pois quando você desenvolve a
meditação da equanimidade, toda aversão será abandonada.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>22.
“Rahula, desenvolva a meditação das partes do corpo; pois quando você desenvolve
a meditação das partes do corpo, toda cobiça será abandonada.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>23.
“Rahula, desenvolva a meditação da percepção da impermanência; pois quando você
desenvolve a meditação da percepção da impermanência, toda presunção ‘eu sou’
será abandonada.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>24. “Rahula,
desenvolva a atenção plena na respiração. Quando a atenção plena na respiração
é desenvolvida e cultivada, ela traz grandes frutos e grandes benefícios.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>25. “Aqui,
Rahula, um bhikkhu, dirigindo-se à floresta, ou à sombra de uma árvore, ou para
um local isolado, senta-se com as pernas cruzadas, mantém o corpo ereto e
estabelecendo a plena atenção à sua frente, ele inspira com atenção plena
justa, ele expira com atenção plena justa.[<a href="#N7">7</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R7></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>26. “Inspirando longo, ele compreende : ‘eu inspiro longo’; ou
expirando longo, ele compreende: ‘eu expiro longo.’ Inspirando curto, ele
compreende: ‘eu inspiro curto’; ou expirando curto, ele compreende: ‘eu expiro
curto.’ Ele treina assim: ‘eu inspiro experienciando todo o corpo [da
respiração]’; ele treina assim: ‘eu expiro experienciando todo o corpo [da
respiração].’ Ele treina assim: ‘eu inspiro tranqüilizando a formação do corpo
[da respiração]’: ele treina assim: ‘eu expiro tranqüilizando a formação do
corpo [da respiração].’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>27. “Ele
treina assim: ‘eu inspiro experienciando êxtase’; ele treina assim: ‘eu expiro
experienciando êxtase.’ Ele treina assim: ’eu inspiro experienciando a
felicidade’; ele treina assim: ‘eu expiro experienciando a felicidade’. Ele
treina assim: ’eu inspiro experienciando a formação da mente;’ ele treina
assim: ‘eu expiro experienciando a formação da mente.’ Ele treina assim:
‘eu inspiro tranqüilizando a formação da mente’; ele treina assim: ‘eu
expiro tranqüilizando a formação da mente.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>28. “Ele
treina assim: ‘eu inspiro experienciando a mente’; ele treina assim: ‘eu expiro
experienciando a mente.’ Ele treina assim: ‘eu inspiro satisfazendo a
mente’;<span style='mso-spacerun:yes'>  </span>ele treina assim: ‘eu expiro
satisfazendo a mente.’ Ele treina assim: ‘eu inspiro concentrando a mente’; ele
treina assim: ‘eu expiro concentrando a mente.’ Ele treina assim: ‘eu inspiro
libertando a mente’; ele treina assim: ‘eu expiro libertando a mente.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>29. “Ele
treina assim: ‘eu<span style='mso-spacerun:yes'>  </span>inspiro contemplando a
impermanência’; ele treina assim: ‘eu expiro contemplando a impermanência.’ Ele
treina assim: ‘eu inspiro contemplando o desaparecimento’; ele treina assim:
‘eu expiro contemplando o desaparecimento.’ Ele treina assim: ‘eu inspiro
contemplando a cessação’; ele treina assim: ‘eu expiro contemplando a
cessação.’ Ele treina assim: ‘eu inspiro contemplando a renúncia’, ele treina
assim: ‘eu expiro contemplando a renúncia.’ <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>30.
“Rahula, assim é como a atenção plena na respiração é desenvolvida e cultivada,
para que traga grandes frutos e grandes benefícios. Quando a atenção plena na
respiração é assim desenvolvida e cultivada, até mesmo a última inspiração e
expiração são percebidas no momento em que cessam, elas não são ignoradas.” [<a
href="#N8">8</a>]<o:p></o:p></span></p>


<p class=Normal><a name=R8></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Isso foi o que disse o Abençoado. O venerável Rahula ficou satisfeito e
contente com as palavras do Abençoado.<o:p></o:p></span></p>

