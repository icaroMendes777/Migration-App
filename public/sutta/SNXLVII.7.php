<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
no Himalaia, o rei dentre as montanhas, há regiões difíceis, irregulares onde
nem os macacos nem os seres humanos podem ir. Há regiões difíceis, irregulares
onde macacos podem ir mas não os seres humanos. Há terras planas, adoráveis,
onde ambos, os macacos e os seres humanos podem ir. Lá, ao longo das trilhas
dos macacos, caçadores colocam armadilhas com piche para capturar os macacos. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqueles
macacos que não são tolos ou descuidados, ao verem uma armadilha, se mantêm à
distância. Mas outros macacos que são tolos e descuidados vão até a armadilha e
a agarram com a mão; assim ele fica preso. Pensando, ‘eu vou libertar a minha
mão,’ ele agarra a armadilha com a outra mão. Essa também fica presa. Pensando,
‘eu vou libertar ambas as mãos,’ ele agarra a armadilha com o pé . Este também
fica preso. Pensando, ‘eu libertarei ambos, as mãos e o pé,’ ele agarra a
armadilha com o outro pé. E este também fica preso. Pensando, ‘eu libertarei
ambos, as mãos e os pés,’ ele agarra a armadilha com a boca. E esta também fica
presa. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Assim,
bhikkhus, aquele macaco, preso por cinco pontos, fica ali deitado guinchando.
Ele deu de encontro com a calamidade e o desastre, e o caçador poderá fazer o
que quiser com ele. O caçador espeta-o, prende-o ao mesmo bloco de madeira e
parte para onde ele quiser.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Isso é o que acontece com
qualquer um que vagueie fora do seu domínio, no território dos outros.<o:p></o:p></span></p>

<p class=Normal><a name=N6></a><a name=N18></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Portanto, bhikkhus, não vagueiem fora do seu domínio, no território dos
outros. Mara ganhará acesso àqueles que vagueiam fora do seu domínio, no
território dos outros, Mara irá controlá-los. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual não é, para um
bhikkhu, o seu domínio, mas o território dos outros? Os cinco elementos do prazer sensual. Quais cinco? As formas percebidas pelo olho que são desejáveis,
agradáveis e fáceis de serem gostadas, conectadas com o desejo sensual e que
provocam a cobiça. Sons percebidos pelo ouvido … Aromas percebidos pelo nariz …
Sabores percebidos pela língua …Tangíveis percebidos pelo corpo que são
desejáveis, agradáveis e fáceis de serem gostados, conectados com o desejo
sensual e que provocam a cobiça. Isso, para um bhikkhu, não é o seu domínio,
mas o território dos outros.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Movam-se, bhikkhus, dentro
do seu domínio, do seu território ancestral. Mara não ganhará acesso àqueles
que se movem dentro do seu domínio, do seu território ancestral, Mara não os
controlará. E qual é, para um bhikkhu, o seu domínio, o seu território
ancestral? Os quatro fundamentos da atenção plena. Quais quatro? Aqui,
bhikkhus, um bhikkhu permanece contemplando o corpo como um corpo, ardente,
plenamente consciente e com atenção plena, tendo colocado de lado a cobiça e o
desprazer pelo mundo. Ele permanece contemplando as sensações como sensações
... a mente como mente ... os objetos mentais como objetos mentais, ardente,
plenamente consciente e com atenção plena, tendo colocado de lado a cobiça e o
desprazer pelo mundo. Isto é, para um bhikkhu, o seu domínio, o seu território
ancestral.”</span><a name=N23></a></p>


