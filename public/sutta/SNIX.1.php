<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim ouvi.
Em certa ocasião, um certo bhikkhu estava entre os Kosalas numa certa floresta.
Agora, naquela ocasião, enquanto aquele bhikkhu estava no seu retiro diário,
ele não conseguia deixar de pensar pensamentos inábeis conectados
com a vida em família.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, uma devata que habitava aquela floresta, sentindo compaixão por aquele
bhikkhu, desejando o bem dele, desejando estimular nele um senso de urgência,
foi até onde ele estava e recitou os seguintes versos:<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Desejando o retiro você se isolou na floresta, <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>no entanto a sua mente flui para fora.<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Remova, homem, o desejo pelas pessoas;<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>então você será feliz, desprovido de cobiça.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Você tem de abandonar o descontentamento, ter atenção plena -<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>deixe que eu lhe recorde o caminho do bem.<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Difícil de cruzar, de fato, é o abismo empoeirado;<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>não permita que a poeira sensual o subjugue.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Tal qual um passarinho sujo de terra <br>

