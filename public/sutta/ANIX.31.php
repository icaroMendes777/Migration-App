<p class=Normal><b>Notas:</b></p>

<p class=Normal> <a name=N1></a> [1] <i>Kamasañña niruddha</i>. <i>Niruddha</i> significa cessar, dissolver, desaparecer. <i>Kama</i> pode ter um significado objetivo ou subjetivo. O seu significado objetivo são os objetos dos sentidos, o significado subjetivo é o prazer e a alegria causados pelos objetos dos sentidos. O desaparecimento de <i>kama</i> significa que os objetos dos sentidos bem como o prazer e a alegria por eles causados desaparecem no primeiro jhana ou em outras palavras, os sentidos cessam.</p>

