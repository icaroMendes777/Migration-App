<p class=Normal> "Bhikkhus, há essas nove cessações sucessivas. Quais nove? (1) Naquele que alcançou o primeiro jhana, a percepção sensorial cessa. [<a href="#N1">1</a>] (2) Naquele que alcançou o segundo jhana, o pensamento aplicado e o pensamento sustentado cessam. (3) Naquele que alcançou o terceiro jhana, o êxtase cessa. (4) Naquele que alcançou o quarto jhana, a inspiração e a expiração cessam. (5) Naquele que alcançou a base do espaço infinito, a percepção da forma cessa. (6) Naquele que alcançou a base da consciência infinita, a percepção da base do espaço infinito cessa. (7) Naquele que alcançou a base do nada, a percepção da base da consciência infinita cessa. (8) Naquele que alcançou a base da nem percepção, nem não percepção, a percepção da base do nada cessa. (9) Naquele que alcançou a cessação da percepção e sensação, a percepção e sensação cessam. Essas, bhikkhu, são as nove cessações sucessivas."</p>

