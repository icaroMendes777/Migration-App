<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] </span>MA, tomando o uso que o venerável Sariputta faz da palavra
“pessoa” <i>(puggala)</i>, explica que o Buda possui um ensinamento duplo – o
ensinamento convencional <i>(sammutidesana)</i> expresso em termos de pessoas,
seres, mulheres e homens, etc., e o ensinamento último <i>(paramatthadesana)</i>
expresso apenas em termos que possuem validade ontológica última, tais como
agregados, elementos, bases dos sentidos, impermanência, sofrimento, não-eu,
etc. O Buda expõe o ensinamento através de qualquer abordagem que seja mais
adequada para permitir que o ouvinte penetre o significado, dissipe a delusão e
alcance a distinção. O uso da palavra “pessoa,” portanto, não implica a
concepção errada da pessoa como um eu. [<a href="#R1">Retorna</a>]</p>

