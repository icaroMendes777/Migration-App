<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no
Parque de Anathapindika.<a name=T2></a> Lá o</span> venerável Sariputta se
dirigiu aos monges desta forma: “Amigos, bhikkhus.” – “Amigo,” eles
responderam. O venerável<span style="mso-spacerun: yes">  </span>Sariputta
disse o seguinte:</p>



<p class=Normal>2. “Amigos, existem esses quatro tipos de pessoas que podem
ser encontradas no mundo.[<a href="#N1">1</a>] <a name=R1></a>Quais quatro?
Aqui uma pessoa com mácula não compreende como na verdade é que: ‘Eu tenho
mácula em mim.’ Aqui uma pessoa com mácula compreende como na verdade é
que: ‘Eu tenho mácula em mim.’ Aqui uma pessoa sem mácula não
compreende como na verdade é que: ‘Eu não tenho mácula em mim.’ Aqui uma pessoa
sem mácula compreende como na verdade é que: ‘Eu não tenho mácula em mim.’</p>



<p class=Normal>“Neste caso, a pessoa com mácula que não compreende como
na verdade é que: ‘Eu tenho mácula em mim’ é chamada de inferior dentre
aquelas duas pessoas que possuem mácula. Neste caso, a pessoa com mácula
que compreende como na verdade é que: ‘Eu tenho mácula em mim’ é chamada de
superior entre aquelas duas pessoas que possuem mácula.</p>



<p class=Normal>“Neste caso, a pessoa sem mácula que não compreende como na
verdade é que: ‘Eu não tenho mácula em mim’ é chamada de inferior entre aquelas duas
pessoas que não possuem mácula. Neste caso, a pessoa sem mácula que compreende
como na verdade é que: ‘Eu não tenho mácula em mim’ é chamada de superior entre
aquelas duas pessoas que não possuem mácula.”</p>



<p class=Normal>3. Quando isso foi dito o venerável Maha Moggallana
perguntou ao venerável Sariputta: “Amigo Sariputta, qual é a causa e razão
porque, dessas duas pessoas com mácula, uma é chamada de inferior e a outra é
chamada de superior? Qual é a causa e razão porque, dessas duas pessoas sem
mácula, uma é chamada de inferior e a outra é chamada de superior?”</p>



<p class=Normal>4. “Neste caso, amigo, quanto à pessoa com mácula que
não compreende como na verdade é que: ‘Eu tenho mácula em mim,’ pode-se
esperar que ela não irá despertar o zelo, fazer o esforço, ou estimular a energia
para abandonar a mácula e que ela irá morrer com cobiça, raiva e delusão,
com a mácula, com a mente impura. Suponha que uma travessa de bronze fosse
trazida de uma loja ou do forjador coberta com sujeira e manchas e o dono nem a
usasse nem a limpasse mas colocasse num canto empoeirado. Dessa forma, mais
tarde, a travessa de bronze ficaria ainda mais suja e manchada? – “Sim, amigo.”
– “ Da mesma forma, amigo, quando uma pessoa com mácula não compreende como
na verdade é que: ‘Eu tenho mácula em mim,’ pode-se esperar … que ela irá
morrer … com a mente impura.</p>



<p class=Normal><a name=T5></a>5. “Neste caso, quanto à pessoa com 
mácula que compreende como na verdade é que: ‘Eu tenho mácula em mim,’
pode-se esperar que ela irá despertar o zelo, fazer o esforço, ou estimular a
energia para abandonar a mácula e que ela irá morrer sem cobiça, raiva e
delusão, sem mácula, com a mente pura. Suponha que uma travessa de bronze fosse
trazida de uma loja ou do forjador coberta com sujeira e manchas e o dono a
limpasse e não a colocasse num canto empoeirado. Dessa forma, mais tarde, a
travessa de bronze ficaria ainda mais limpa e brilhante? – “Sim, amigo.” – “ Da
mesma forma, amigo, quando uma pessoa com mácula compreende como na verdade
é que: ‘Eu tenho mácula em mim,’ pode-se<span style="mso-spacerun: yes"> 
</span>esperar … que ela irá morrer … com a mente pura. </p>



<p class=Normal>6. “Neste caso, quanto à pessoa sem mácula que não
compreende como na verdade é que: ‘Eu não tenho mácula em mim,’ pode-se<span
style="mso-spacerun: yes">  </span>esperar que ela irá dar atenção ao sinal da
beleza, [<a href="#N2">2</a>] <a name=R2></a>e que ao fazer isso o desejo irá
infectar a sua mente e que ela irá morrer com cobiça, raiva e delusão, com 
mácula, com a mente impura. Suponha que uma travessa de bronze fosse trazida da
loja ou do forjador limpa e brilhante e o dono nem a usasse nem a limpasse mas
a colocasse num canto empoeirado. Dessa forma, mais tarde, a travessa de bronze
ficaria ainda mais manchada e suja?” – “Sim, amigo.” – “Da mesma forma, amigo,
quando uma pessoa sem mácula não compreende como na verdade é que: ‘Eu não
tenho mácula em mim,’ pode-se esperar que ela irá morrer … com a mente   impura.</p>



<p class=Normal>7. &quot;Neste caso, quanto à pessoa sem mácula que
compreende como na verdade é que: ‘Eu não tenho mácula em mim,’ pode-se<span
style="mso-spacerun: yes">  </span>esperar que ela não irá dar atenção ao sinal
da beleza e que ao não fazer isso o desejo não irá infectar a sua mente e que
ela irá morrer sem cobiça, raiva e delusão, sem mácula, com a mente pura.
Suponha que uma travessa de bronze fosse trazida da loja ou do forjador limpa e
brilhante e o dono a usasse e a limpasse e não a colocasse num canto
empoeirado. Dessa forma, mais tarde, a travessa de bronze ficaria ainda mais
limpa e brilhante?” – “Sim, amigo.” – “Da mesma forma, amigo, quando uma pessoa
sem mácula compreende como na verdade é que: ‘Eu não tenho mácula em mim,’
pode-se esperar … que ela irá morrer … com a mente pura.</p>

<p class=Normal><span style="mso-spacerun: yes"> </span></p>

<p class=Normal>8. &quot;Essa é a causa e razão porque, das duas pessoas com
mácula, uma é chamada de inferior e a outra é chamada de superior. Essa é a
causa e razão porque, das duas pessoas sem mácula, uma é chamada de inferior e
a outra é chamada de superior.</p>



<p class=Normal>9. “’Mácula, mácula,’ é dito, amigo, mas o que significa
essa palavra ‘mácula’? ‘Mácula,’ amigo, é um termo para a esfera dos desejos
ruins e prejudiciais.</p>



<p class=Normal><a name=T10></a>10. “É possível que um bhikkhu desejasse:
‘Se eu cometer uma transgressão, que os bhikkhus não saibam que eu cometi uma transgressão.’
E é possível que os bhikkhus venham a saber que aquele bhikkhu cometeu uma
transgressão. Por isso, ele fica enraivecido e amargo assim: “Os bhikkhus sabem
que eu cometi uma transgressão.’ A raiva e amargor são ambos uma mácula.</p>



<p class=Normal>11. “É possível que um bhikkhu desejasse: ‘Eu cometi uma
transgressão. Os bhikkhus deveriam me admoestar em particular, não no meio da
Sangha.’ E é possível que os bhikkhus admoestem aquele bhikkhu no meio da
Sangha, não em particular. Por isso ele fica enraivecido e amargo assim: “Os
bhikkhus me admoestaram no meio da Sangha, não em particular.’ A raiva e
amargor são ambos uma mácula.</p>



<p class=Normal>12. “É possível que um bhikkhu desejasse: ‘Eu cometi uma
transgressão. Uma pessoa que seja meu par deveria me admoestar, não uma pessoa
que não seja o meu par.’ E é possível que uma pessoa que não seja o seu par o
admoeste, não o seu par. Por isso ele fica enraivecido e amargo assim: ‘Uma
pessoa que não é o meu par me admoestou, não o meu par.’ A raiva e amargor são
ambos uma mácula.</p>



<p class=Normal>13. “É possível que um bhikkhu desejasse: ‘Oh que o Mestre
ensine o Dhamma para os bhikkhus perguntando a mim uma série de questões, não
um outro bhikkhu!’ E é possível que o Mestre ensine o Dhamma aos bhikkhus
perguntando uma série de questões a um outro bhikkhu, não àquele bhikkhu. Por
isso ele fica enraivecido e amargo assim: ‘O Mestre ensina o Dhamma aos
bhikkhus perguntando uma série de questões aos outros bhikkhus, não a mim.’ A
raiva e amargor são ambos uma mácula.</p>



<p class=Normal>14. “É possível que um bhikkhu desejasse: ‘Oh que os
bhikkhus entrem no vilarejo para esmolar alimentos colocando-me no começo da
fila, não um outro bhikkhu!’ E é possível que os bhikkhus entrem no vilarejo
para esmolar alimentos colocando um outro bhikkhu no começo da fila, não aquele
bhikkhu. Por isso ele fica enraivecido e amargo assim: ‘Os bhikkhus entraram no
vilarejo para esmolar alimentos colocando um outro bhikkhu no começo da fila,
não a mim.' A raiva e amargor são ambos uma mácula.</p>

<p class=Normal><span style="mso-spacerun: yes"> </span></p>

<p class=Normal>15. “É possível que um bhikkhu desejasse: 'Oh que eu possa
obter o melhor assento, a melhor água, a melhor comida esmolada no refeitório,
não um outro bhikkhu!’ E é possível que um outro bhikkhu obtenha o melhor
assento…</p>



<p class=Normal>16. “É possível que um bhikkhu desejasse: 'Oh que eu possa
dar as bênçãos no refeitório após a refeição, não um outro bhikkhu!' E é
possível que um outro bhikkhu dê as bênçãos…</p>



<p class=Normal>17-20. “É possível que um bhikkhu desejasse: 'Oh que eu
possa ensinar o Dhamma para os bhikkhus ... que eu possa ensinar o Dhamma para
as bhikkhunis…discípulos leigos homens ... discípulos leigos mulheres ... em visita
ao monastério, não um outro bhikkhu!’ E é possível que um outro bhikkhu ensine
o Dhamma...</p>



<p class=Normal>21-24. “É possível que um bhikkhu desejasse: 'Oh que os
bhikkhus ... bhikkhunis ... discípulos leigos ... possam honrar, respeitar,
reverenciar e venerar a mim, não a um outro bhikkhu!' E é possível que eles
honrem ... a um outro bhikkhu…</p>



<p class=Normal>25-28. “É possível que um bhikkhu desejasse: 'Oh que eu
possa ser aquele que ganha um manto superior ... comida esmolada superior ...
moradia superior ... medicamentos superiores ... não um outro bhikkhu!’ E é possível
que algum outro bhikkhu seja aquele que ganha medicamentos superiores não
aquele bhikkhu. Por isso ele fica enraivecido e amargo assim: 'Um outro bhikkhu
é aquele que ganhou os medicamentos superiores, não eu.’ ’Mácula,’ amigo, é um
termo para a esfera dos desejos ruins e prejudiciais.</p>



<p class=Normal>29. “Se a esfera desses desejos ruins e prejudiciais é
vista e ouvida como não tendo sido abandonada por um bhikkhu, então apesar
de ele viver na floresta, freqüentar locais afastados, alimentar-se de comida
esmolada, esmolando de casa em casa, vestindo-se com trapos, vestindo-se com
mantos grosseiros,[<a href="#N3">3</a>] <a name=R3></a>ainda assim os seus
companheiros na vida santa não irão honrá-lo, respeitá-lo, reverenciá-lo e
venerá-lo. Por que isso? Porque a esfera desses desejos ruins e prejudiciais
é vista e ouvida como não tendo sido abandonada por aquele venerável.</p>



<p class=Normal>“Suponha que uma travessa de metal seja trazida de uma loja
ou forjador limpa e brilhante e o dono colocasse nela a carcaça de uma cobra,
ou de um cão, ou de um ser humano, cobrisse com uma tampa e regressasse ao mercado; então
as pessoas ao verem isso diriam: ‘O que é que você está carregando como se
fosse um tesouro?’ Então levantando a tampa elas olhariam para aquilo e assim
que vissem seriam influenciadas por tal abominação, repugnância e náusea que
até mesmo aquelas que estivessem famintas perderiam a fome, sem falar daquelas
que já houvessem comido.</p>



<p class=Normal>“Da mesma forma, se a esfera desses desejos ruins e
prejudiciais é vista e ouvida como não tendo sido abandonada por um
bhikkhu, então apesar de ele viver na floresta ... não tendo sido abandonada
por aquele venerável.</p>



<p class=Normal>30. “Se a esfera desses desejos ruins e prejudiciais é
vista e ouvida como tendo sido abandonada por um bhikkhu, então apesar dele
viver no vilarejo, de aceitar convites, usar mantos que foram dados por chefes
de família, [<a href="#N4">4</a>] <a name=R4></a>ainda assim os seus
companheiros na vida santa irão honrá-lo, respeitá-lo, reverenciá-lo e
venerá-lo. Por que isso? Porque a esfera desses desejos ruins e prejudiciais
é vista e ouvida como tendo sido abandonada por aquele venerável.</p>



<p class=Normal>“Suponha que uma travessa de metal seja trazida de uma loja ou
forjador limpa e brilhante e o dono colocasse nela arroz limpo cozido com
vários tipos de molhos e caril, e a cobrisse com uma tampa e regressasse ao
mercado; então as pessoas ao verem isso diriam: ‘O que é que você está
carregando como se fosse um tesouro?’ Então levantando a tampa elas olhariam
para aquilo e assim que vissem seriam influenciadas por tal apreço, apetite e
deleite que até mesmo aquelas que já houvessem comido gostariam de comer, sem
falar daquelas que estivessem famintas.</p>



<p class=Normal>“Da mesma forma, se a esfera desses desejos ruins e
prejudiciais é vista e ouvida como tendo sido abandonada por um
bhikkhu, então apesar de ele viver no vilarejo ... tendo sido abandonada por
aquele venerável.”</p>



<p class=Normal>31. Quando isso foi dito, o venerável Maha Moggallana disse
para o venerável Sariputta: “Um símile me vem à mente, amigo Sariputta.” –
“Diga, amigo Moggallana.” Em certa ocasião, amigo, eu estava vivendo no Forte
da Colina, em Rajagaha. Então, ao amanhecer, eu me vesti e tomando minha tigela
e manto externo, fui para Rajagaha para esmolar alimentos. Agora naquela
ocasião Samiti, o filho do construtor de carruagens, estava aplainando uma roda
e o Ajivaka Panduputta, filho de um antigo construtor de carruagens, estava em
pé ao lado. [<a href="#N5">5</a>] <a name=R5></a>Então o seguinte pensamento
surgiu na mente do Ajivaka Panduputta: 'Ah! que Samiti, filho do construtor de
carruagens, consiga aplainar essa curva, essa deformação, essa falha dessa
roda, para que ela se torne sem curvas, deformações ou falhas e que consista
apenas de pura madeira.’ E, ao mesmo tempo que esse pensamento ocorreu na sua
mente, Samiti o filho do construtor de carruagens aplainou aquela curva, aquela
deformação, aquela falha da roda. Então o Ajivaka Panduputta, filho de um
antigo construtor de carruagens, ficou contente e expressou sua alegria da
seguinte forma: 'Ele aplaina como se conhecesse o meu coração com o seu
coração!'</p>



<p class=Normal><a name=T32></a>32. “Da mesma forma, amigo, existem pessoas
que não possuem fé e que deixaram a vida em família pela vida santa não pela fé
mas em busca de um modo de vida, que são fraudulentas, enganadoras, traidoras,
arrogantes, vazias, vaidosas, com a linguagem rude, com a língua solta;
descuidadas das faculdades dos sentidos, sem moderação no comer, sem se dedicar
à vigilância, que não se interessam pelo isolamento, sem grande respeito pelo
treinamento, luxuriosas, descuidadas,<span style="mso-spacerun: yes"> 
</span>líderes em decair, negligenciam o isolamento, preguiçosas, carentes de
energia, sem atenção plena, sem plena consciência, sem concentração, com as
mentes distraídas, desprovidas de sabedoria, tolas. O venerável Sariputta com
este discurso do Dhamma aplaina as suas falhas tal como se conhecesse o meu
coração com o seu coração![<a href="#N6">6</a>]</p>



<p class=Normal><a name=R6></a>“Mas existem membros de clãs que deixaram a
vida em família pela vida santa, pela fé, que não são fraudulentos,
não são enganadores, não são traidores, não são arrogantes, não são vazios, não são vaidosos, não têm a linguagem rude,
não têm a língua solta; cuidam das faculdades dos sentidos, são moderados no
comer, dedicados à vigilância, interessados no isolamento, com grande respeito
pelo treinamento, não são luxuriosos ou descuidados, são perspicazes ao evitar
o decair, líderes no isolamento, energéticos, decididos, com a atenção plena
estabelecida, plenamente conscientes, concentrados, com as mentes unificadas,
com sabedoria, sábios. Esses, ao ouvirem o discurso do Dhamma do venerável
Sariputta, bebem-no e comem-no, por assim dizer, através da palavra e do
pensamento. É de fato bom que ele faça os seus companheiros na vida santa
emergirem do que é prejudicial e que se estabeleçam naquilo que é benéfico.</p>



<p class=Normal>33. &quot;Assim como uma mulher - ou um homem - jovem, pleno
de juventude, que gosta de ornamentos, com a cabeça banhada, que ao receber uma
grinalda de flores de lótus ou jasmim, ou rosas, toma-a com as duas mãos e a
coloca sobre a cabeça, da mesma forma, há membros de clãs que deixaram a vida
em família pela vida santa, pela fé ... sábios. Esses, ao ouvirem o discurso do
Dhamma do venerável Sariputta, bebem-no e comem-no, por assim dizer, através da
palavra e do pensamento. É de fato bom que ele faça os seus companheiros na
vida santa emergirem do que é prejudicial e que se estabeleçam naquilo que é
benéfico.&quot;</p>



