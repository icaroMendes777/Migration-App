<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
enquanto o nobre conhecimento não tiver surgido no nobre discípulo, ainda não
haverá estabilidade nas [outras] quatro faculdades, não haverá firmeza nas [outras]
quatro faculdades.[<a href="#N1">1</a>] <a name=R1></a>Mas quando o nobre
conhecimento surgiu no nobre discípulo, então haverá estabilidade nas [outras]
quatro faculdades, haverá firmeza nas [outras] quatro faculdades.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
é como uma casa com um telhado com cumeeira: enquanto a cumeeira não for
colocada no seu lugar, não haverá estabilidade nos caibros, não haverá firmeza
nos caibros; mas quando a cumeeira tiver sido colocada no seu lugar, então
haverá estabilidade nos caibros, então haverá firmeza nos caibros. Da mesma
forma, bhikkhus, enquanto o nobre conhecimento não tiver surgido no nobre
discípulo, ainda não haverá estabilidade nas [outras] quatro faculdades, não
haverá firmeza nas [outras] quatro faculdades. Mas quando o nobre conhecimento surgir
no nobre discípulo, então haverá estabilidade nas [outras] quatro faculdades,
haverá firmeza nas [outras] quatro faculdades.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quais
quatro? A faculdade da convicção, a faculdade da energia, a faculdade da
atenção plena, a faculdade da concentração. No caso de um nobre discípulo que
possui sabedoria, a convicção que resulta dela se torna estável; a energia que
resulta dela se torna estável; a atenção plena que dela resulta se torna
estável; a concentração que dela resulta se torna estável.”<o:p></o:p></span></p>



