<p class=Tit3 align=center style='text-align:center'><b>Samyutta Nikaya XLVI.54</b></Tit3>
<p class=Tit1 align=center style='text-align:center'>Mettasahagata Sutta </Tit1>
<p class=Tit1 align=center style='text-align:center'>Acompanhado pelo Amor Bondade </Tit1>

<p class=Normal align=center style='text-align:center'><span lang=PT-BR style='font-size:
