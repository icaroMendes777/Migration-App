<p class=Normal style='margin-left:.75in'>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em certa
ocasião, o Abençoado estava entre os Koliyas numa cidade denominada Haliddavasana.
Então, ao amanhecer, vários bhikkhus se vestiram e tomando as suas tigelas e os
mantos externos foram para Haliddavasana para esmolar alimentos. Então, eles
pensaram: “Ainda é muito cedo para esmolar alimentos em Haliddavasana. E se nós
fossemos até o parque dos errantes de outras seitas,” e assim eles foram até o
parque dos errantes de outras seitas e ao chegar os cumprimentaram. Quando a
conversa amigável e cortês havia terminado, eles sentaram a um lado. Os
errantes lhes disseram: <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigos, o
contemplativo Gotama ensina o Dhamma aos seus discípulos deste modo: ‘Venham,
bhikkhus, abandonem os cinco obstáculos, as corrupções da mente que enfraquecem
a sabedoria e permaneçam permeando o primeiro quadrante com a mente imbuída de
amor bondade, do mesmo modo o segundo, do mesmo modo o terceiro, do mesmo modo
o quarto; assim acima, abaixo, em volta e em todos os lugares, para todos bem
como para si mesmos, permaneçam permeando o mundo todo com a mente imbuída de
amor bondade, abundante, transcendente, imensurável, sem hostilidade e sem má
vontade. Permaneçam permeando o primeiro quadrante com a mente imbuída de
compaixão, do mesmo modo o segundo, do mesmo modo o terceiro, do mesmo modo o
quarto; assim acima, abaixo, em volta e em todos os lugares, para todos bem
como para si mesmos, permaneçam permeando o mundo todo com a mente imbuída de
compaixão, abundante, transcendente, imensurável, sem hostilidade e sem má
vontade. Permaneçam permeando o primeiro quadrante com a mente imbuída de
alegria altruísta, do mesmo modo o segundo, do mesmo modo o terceiro, do mesmo
modo o quarto; assim acima, abaixo, em volta e em todos os lugares, para todos
bem como para si mesmos, permaneçam permeando o mundo todo com a mente imbuída
de alegria altruísta, abundante, transcendente, imensurável, sem hostilidade e
sem má vontade. Permaneçam permeando o primeiro quadrante com a mente imbuída
de equanimidade, do mesmo modo o segundo, do mesmo modo o terceiro, do mesmo
modo o quarto; assim acima, abaixo, em volta e em todos os lugares, para todos
bem como para si mesmos, permaneçam permeando o mundo todo com a mente imbuída
de equanimidade, abundante, transcendente, imensurável, sem hostilidade e sem
má vontade.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Nós
também, amigos, ensinamos aos nossos discípulos desta maneira: ‘Venham, amigos,
abandonem os cinco obstáculos ... (igual acima) ... permaneçam permeando o
mundo todo com a mente imbuída de amor bondade ... compaixão ... alegria
altruísta ... equanimidade ... sem má vontade.’ Qual é então a distinção,
amigos, qual é a variação, qual é a diferença entre o ensinamento do Dhamma do
contemplativo Gotama e o nosso, entre as instruções dele e as nossas?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então,
aqueles bhikkhus nem aprovaram nem desaprovaram as palavras dos errantes. Sem
dizer isto ou aquilo eles se levantaram dos seus assentos e foram embora,
pensando: “Devemos entender o significado dessas palavras na presença do
Abençoado.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Depois de
terem esmolado alimentos em Haliddavasana e retornado, após a refeição eles
foram até o Abençoado e depois de cumprimentá-lo sentaram a um lado e relataram
o que havia ocorrido. [O Abençoado disse:]<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
os errantes de outras seitas que assim falam devem ser questionados da seguinte
forma: ‘Amigos, como é desenvolvida a libertação da mente através do amor bondade?
Qual é o seu destino, o seu clímax, o seu fruto, o seu objetivo último? Como é
desenvolvida a libertação da mente através da compaixão? Qual é o seu destino,
o seu clímax, o seu fruto, o seu objetivo último? Como é desenvolvida a
libertação da mente através da alegria altruísta? Qual é o seu destino, o seu
clímax, o seu fruto, o seu objetivo último? Como é desenvolvida a libertação da
mente através da equanimidade? Qual é o seu destino, o seu clímax, o seu fruto,
o seu objetivo último?’ Sendo questionados dessa forma, os errantes de outras
seitas fracassarão na exposição desse assunto, e mais ainda, eles se meterão em
dificuldades. Por que isso? Porque não é o território deles. Bhikkhus, eu não
vejo ninguém no mundo com os seus devas, Maras e Brahmas, esta população com os
seus contemplativos e brâmanes, seus príncipes e<span style="mso-spacerun:
yes">  </span>povo, que pudesse satisfazer a mente com uma resposta a essas
questões, exceto o Tathagata ou os seus discípulos, ou alguém que tenha
aprendido com eles.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como,
bhikkhus, é desenvolvida a libertação da mente através do amor bondade? Qual é
o seu destino, o seu clímax, o seu fruto, o seu objetivo último? Nesse caso,
bhikkhus, um bhikkhu desenvolve o fator da iluminação da atenção plena
acompanhado pelo amor bondade ... ele desenvolve o fator da iluminação da
equanimidade acompanhado pelo amor bondade, que tem como base o afastamento,
desapego e cessação, que amadurece no abandono. [<a href="#N1">1</a>] <a
name=R1></a>Se ele desejar: ‘Que eu permaneça percebendo o repulsivo no não
repulsivo,’ ele permanece percebendo o repulsivo naquilo. Se ele desejar: ‘Que
eu permaneça percebendo o não repulsivo no repulsivo,’ ele permanece percebendo
o não repulsivo naquilo. Se ele desejar: ‘Que eu permaneça percebendo o
repulsivo no não repulsivo e no repulsivo,’ ele permanece percebendo o
repulsivo naquilo. Se ele desejar: ‘Que eu permaneça percebendo o não repulsivo
no repulsivo e no não repulsivo,’ ele permanece percebendo o não repulsivo
naquilo. Se ele desejar: ‘Evitando ambos, o não repulsivo e o repulsivo, que eu
permaneça equânime, com atenção plena e plena consciência,’ então, ele
permanece assim equânime, com atenção plena e plena consciência. [<a href="#N2">2</a>]
<a name=R2></a>Ou então, ele entra e permanece na libertação do belo. Bhikkhus,
a libertação da mente através do amor bondade tem o belo como seu clímax, eu
digo, para um bhikkhu sábio, aqui, que não tenha penetrado uma libertação
superior.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como,
bhikkhus, é desenvolvida a libertação da mente através da compaixão? Qual é o
seu destino, o seu clímax, o seu fruto, o seu objetivo último? Nesse caso,
bhikkhus, um bhikkhu desenvolve o fator da iluminação da atenção plena
acompanhado pela compaixão ... ele desenvolve o fator da iluminação da
equanimidade acompanhado pela compaixão que tem como base o isolamento,
desapego e cessação, que<span style="mso-spacerun: yes">  </span>amadurece na
libertação. Se ele desejar: ‘Que eu permaneça percebendo o repulsivo no não
repulsivo,’ ele permanece percebendo o repulsivo naquilo ... Se ele desejar:
‘Evitando ambos, o não repulsivo e o repulsivo, que eu permaneça equânime, com
atenção plena e plena consciência,’ então, ele permanece assim equânime, com
atenção plena e plena consciência. Ou então, com a completa superação das
percepções da forma, com o desaparecimento das percepções do contato sensorial,
sem dar atenção às percepções da diversidade, consciente de que o ‘espaço é
infinito’ ele entra e permanece na base do espaço infinito. Bhikkhus, a
libertação da mente através da compaixão tem a base do espaço infinito como seu
clímax, eu digo, para um bhikkhu sábio, aqui, que não tenha penetrado uma
libertação superior.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como,
bhikkhus, é desenvolvida a libertação da mente através da alegria altruísta?
Qual é o seu destino, o seu clímax, o seu fruto, o seu objetivo último? Nesse
caso, bhikkhus, um bhikkhu desenvolve o fator da iluminação da atenção plena
acompanhado pela alegria altruísta ... ele desenvolve o fator da iluminação da
equanimidade acompanhado pela alegria altruísta, que tem como base o
isolamento, desapego e cessação, que amadurece na libertação. Se ele desejar:
‘Que eu permaneça percebendo o repulsivo no não repulsivo,’ ele permanece
percebendo o repulsivo naquilo ... Se ele desejar: ‘Evitando ambos, o não
repulsivo e o repulsivo, que eu permaneça equânime, com atenção plena e plena consciência,’
então, ele permanece assim equânime, com atenção plena e plena consciência. Ou
então, com a completa superação da base do espaço infinito, consciente de que a
‘consciência é infinita,’ ele entra e permanece na base da consciência
infinita. Bhikkhus, a libertação da mente através da alegria altruísta tem a
base da consciência infinita como seu clímax, eu digo, para um bhikkhu sábio,
aqui, que não tenha penetrado uma libertação superior.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como,
bhikkhus, é desenvolvida a libertação da mente através da equanimidade? Qual é
o seu destino, o seu clímax, o seu fruto, o seu objetivo último? Nesse caso,
bhikkhus, um bhikkhu desenvolve o fator da iluminação da atenção plena
acompanhado pela equanimidade ... ele desenvolve o fator da iluminação da equanimidade
acompanhado pela equanimidade, que tem como base o isolamento, desapego e
cessação, que amadurece na libertação. Se ele desejar: ‘Que eu permaneça
percebendo o repulsivo no não repulsivo,’ ele permanece percebendo o repulsivo
naquilo ... Se ele desejar: ‘Evitando ambos, o não repulsivo e o repulsivo, que
eu permaneça equânime, com atenção plena e plena consciência,’ então, ele
permanece assim equânime, com atenção plena e plena consciência. Ou então, com
a completa superação da base da consciência infinita, consciente de que ‘não há
nada,’ ele entra e permanece na base do nada. Bhikkhus, a libertação da mente
através da equanimidade tem a base do nada como seu clímax, eu digo, para um
bhikkhu sábio, aqui, que não tenha penetrado uma libertação superior.”<o:p></o:p></span></p>


