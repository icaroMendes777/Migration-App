<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim ouvi.
Em certa ocasião, o Abençoado estava<span style='mso-spacerun:yes'>  </span>em
Vesali no Bosque de Ambapali. Lá ele se dirigiu aos monges desta forma:
“Bhikkhus” –<span style='mso-spacerun:yes'>  </span>“Venerável Senhor,” eles
responderam. O Abençoado disse o seguinte: [<a href="#N1">1</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>“ Bhikkhus,
este é o caminho direto para a purificação dos seres, para superar a tristeza e
a lamentação, para o desaparecimento da dor e da angústia, para alcançar o
caminho verdadeiro, para a realização de Nibbana – isto é, os quatro
fundamentos da atenção plena. Quais são os quatro? <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui, bhikkhus, um bhikkhu
permanece contemplando o corpo como um corpo, ardente, plenamente consciente e
com atenção plena, tendo colocado de lado a cobiça e o desprazer pelo mundo.
Ele permanece contemplando as sensações como sensações, ardente, plenamente
consciente e com atenção plena, tendo colocado de lado a cobiça e o desprazer
pelo mundo. Ele permanece contemplando a mente como mente, ardente, plenamente
consciente e com atenção plena, tendo colocado de lado a cobiça e o desprazer
pelo mundo. Ele permanece contemplando os objetos mentais como objetos mentais,
ardente, plenamente consciente e com atenção plena, tendo colocado de lado a
cobiça e o desprazer pelo mundo.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Esse,
bhikkhus, é o caminho direto para a purificação dos seres, para superar a
tristeza e a lamentação, para o desaparecimento da dor e da angústia, para
alcançar o caminho verdadeiro, para a realização de Nibbana – isto é, os quatro
fundamentos da atenção plena.”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o
que disse o Abençoado. Os bhikkhus ficaram satisfeitos e contentes com as
palavras do Abençoado.<o:p></o:p></span></p>

