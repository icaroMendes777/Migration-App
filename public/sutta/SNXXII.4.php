<p class=Normal>Assim ouvi. Em certa ocasião, o venerável Mahakaccana estava em Avanti no monte Papata, em Kuraraghara. Então, o chefe de família Haliddakani foi até o venerável Mahakaccana e depois de cumprimentá-lo sentou a um lado e disse: </p>

<p class=Normal> “Venerável senhor, isto foi dito pelo Abençoado no Sutta <a href="DN21.php">As Perguntas de Sakka</a>: 'Os contemplativos e brâmanes que estão libertados através da destruição do desejo possuem o conhecimento supremo, estão libertados dos grilhões, perfeitos na vida santa e realizaram o verdadeiro objetivo.' Como, venerável senhor, deve ser entendido em detalhe o significado deste breve enunciado do Abençoado?" </p>

<p class=Normal>"Chefe de família, através da destruição, desapego, cessação, abrir mão, descartar, libertar-se, despegar-se do desejo, da cobiça, do deleite, da adesão, do apego, das fixações mentais, das inclinações ou das obsessões em relação ao elemento forma, é dito que a mente está bem libertada. </p>

<p class=Normal>"Através da destruição, desapego, cessação, abrir mão, descartar, libertar-se, despegar-se do desejo, da cobiça, do deleite, da adesão, do apego, das fixações mentais, das inclinações ou das obsessões em relação ao elemento sensação ... percepção ... formações volitivas ... consciência, é dito que a mente está bem libertada. </p>

<p class=Normal>"Chefe de família, portanto, quando foi dito pelo Abençoado no Sutta As Perguntas de Sakka: 'Os contemplativos e brâmanes que estão libertados através da destruição do desejo possuem o conhecimento supremo, estão libertados dos grilhões, perfeitos na vida santa e realizaram o verdadeiro objetivo,' é desse modo que deve ser entendido em detalhe o significado deste breve enunciado do Abençoado." </p>

