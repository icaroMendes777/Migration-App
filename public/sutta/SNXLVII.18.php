<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Certa
ocasião, o Abençoado estava em Uruvela às margens do rio Neranjara ao pé de uma
figueira-dos-pagodes, pouco tempo depois de ter alcançado a perfeita iluminação.
Então, enquanto o Abençoado estava só em isolamento, um pensamento surgiu na
sua mente: <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Este é o caminho direto
para a purificação dos seres, para superar a tristeza e a lamentação, para o
desaparecimento da dor e da angústia, para alcançar o caminho verdadeiro, para
a realização de Nibbana – isto é, os quatro fundamentos da atenção plena. Quais
são os quatro? Aqui, um bhikkhu permanece contemplando o corpo como um corpo,
ardente, plenamente consciente e com atenção plena, tendo colocado de lado a
cobiça e o desprazer pelo mundo. Ele permanece contemplando as sensações como
sensações ... a mente como mente ... os objetos mentais como objetos mentais,
ardente, plenamente consciente e com atenção plena, tendo colocado de lado a
cobiça e o desprazer pelo mundo. Esse é o caminho direto para a purificação dos
seres ... isto é, os quatro fundamentos da atenção plena.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então,
tendo percebido com a sua<span style="mso-spacerun: yes">  </span>mente o
pensamento na mente do Abençoado – com a mesma rapidez com que um homem forte
pode estender o seu braço flexionado ou flexionar o seu braço estendido - o
Brahma Sahampati desapareceu do mundo de Brahma e reapareceu na
frente do Abençoado.[<a href="#N1">1</a>] <a name=R1></a>Arrumando o seu manto
externo sobre o ombro, ele juntou as mãos numa reverenciosa saudação dizendo:
“Assim é, Abençoado! Assim é,<span style="mso-spacerun: yes"> 
</span>Iluminado! Venerável senhor, este é o caminho direto para a purificação
dos seres ... (igual acima)<span style="mso-spacerun: yes">  </span>... isto é,
os quatro fundamentos da atenção plena.”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o
