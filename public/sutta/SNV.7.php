<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em Savatthi. Então, ao
amanhecer, a bhikkhuni Upacala se vestiu e tomando a tigela e o manto externo
foi para Savatthi esmolar alimentos. Depois de haver esmolado alimentos em
Savatthi e de haver retornado, após a refeição, ela se dirigiu ao bosque dos
Homens Cegos em busca de isolamento.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, Mara, o Senhor do
Mal, desejando despertar medo, trepidação e terror na bhikkhuni Upacala,
desejando que ela abandonasse a sua concentração, se aproximou e disse: “Onde
você deseja renascer, bhikkhuni?” <span style="mso-spacerun: yes"> </span>- “Eu
não desejo renascer em nenhum lugar, amigo.”<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
 “Há os
devas do Tavatimsa e Yama,<br>
e os devas do Tusita,<br>
devas que se deliciam com a criação,<br>
e devas que exercitam o controle.<br>
Dirija a sua mente para lá<br>
e você experimentará o prazer.”<o:p></o:p></span></p>


<p class=Normal style='margin-right:.5in;mso-margin-top-alt:auto;mso-margin-bottom-alt:
