<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR style='mso-ansi-language:PT-BR'>“Há os
devas do Tavatimsa e Yama,<br>
e os devas do Tusita,<br>
devas que se deliciam com a criação,<br>
e devas que exercitam o controle.<br>
Eles ainda estão aprisionados pelo cativeiro da sensualidade,<br>
eles continuam sob o controle de Mara. <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
 “Todo o mundo está
queimando,<br>
todo o mundo está em chamas,<br>
todo o mundo está incandescente,<br>
todo o mundo está estremecendo. <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
 “Aquilo que não
estremece, que não queima,<br>
aquilo no qual as pessoas comuns não buscam abrigo,<br>
onde não existe lugar para Mara:<br>
nisso a minha mente se delicia.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, Mara, o Senhor do
