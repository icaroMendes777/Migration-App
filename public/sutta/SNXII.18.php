<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Notas:<o:p></o:p></span></b></p>


<p class=Normal><a name=N1></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>[1] Se no princípio alguém pensa ‘a sensação e aquele
que sente são idênticos’, então surge a idéia de que o ‘prazer e a dor são
criados pela própria pessoa.’ Pois neste caso a sensação é criada pela própria
sensação, e ao afirmar isso está-se admitindo a existência daquela sensação no
passado. A pessoa declara e se apega à idéia do eternalismo.[<a href="#R1">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N2></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>[2] Se no princípio alguém pensa ‘a sensação é
uma coisa, aquele que sente é outra,’ então surge a idéia que o ‘prazer e a dor
são criados pelos outros,’ que é o que crê aquele que sente a sensação
associada à idéia da aniquilação que então surge: ‘a sensação do agente no
passado foi extinta e alguém outro experimenta os resultados das ações dele.’
Afirmando isso ele declara e se apega à idéia da aniquilação, que o agente é extinto
e que alguém outro renasce. [<a href="#R2">Retorna</a>] <o:p></o:p></span></p>






