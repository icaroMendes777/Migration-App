<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. Então o errante Timbaruka se aproximou do Abençoado e ambos se
cumprimentaram. Depois que a conversa amigável e cortês havia terminado ele
sentou a um lado e disse: <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Como é,
Mestre Gotama: o prazer e a dor são criados pela própria pessoa?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não é
assim, Timbaruka,” o Abençoado respondeu. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
Mestre Gotama, o prazer e a dor são criados pelos outros?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não é
assim, Timbaruka,” o Abençoado respondeu.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Como é
então, Mestre Gotama: o prazer e a dor são criados ambos pela própria pessoa e
pelos outros?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não é
assim, Timbaruka,” o Abençoado respondeu.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então, Mestre
Gotama, o prazer e a dor surgem ao acaso, sem serem criados pela própria pessoa
e tampouco pelos outros?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não é
assim, Timbaruka,” o Abençoado respondeu.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Como é
então, Mestre Gotama: não existe o prazer e a dor?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não é que
não existe o prazer e a dor, Timbaruka; o prazer e a dor existem.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então é
que o Mestre Gotama não conhece e não vê o prazer e a dor?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não é que
eu não conheço e não vejo o prazer e a dor, Timbaruka. Eu conheço o prazer e a
dor, eu vejo o prazer e a dor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ao ser
perguntado: ‘Como é, Mestre Gotama: o prazer e a dor são criados pela própria
pessoa?’ ou ‘Eles são criados pelos outros?’ ou ‘Eles são criados por ambos?’
ou ‘Eles não são criados por nenhum desses’ em cada caso você diz: ‘Não é
assim, Timbaruka.’ <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ao ser
perguntado: ‘Como é então, Mestre Gotama: não existe o prazer e a dor?’ você
diz: ‘Não é que não existe o prazer e a dor, Timbaruka; o prazer e a dor
existem.’ Ao ser perguntado: ‘Então é que o Mestre Gotama não conhece e não vê
o prazer e a dor?’ você diz: ‘Não é que eu não conheço e não vejo o prazer e a
dor, Timbaruka. Eu conheço o prazer e a dor, eu vejo o prazer e a dor.’
Venerável senhor, que o Abençoado me explique o prazer e a dor. Que o Abençoado
me ensine acerca do prazer e da dor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Timbaruka,
[se alguém pensa,] ‘A sensação e aquele que sente são idênticos,’ [então ele
afirma] a respeito de alguém que existe desde o princípio: ‘O prazer e a dor
são criados pela própria pessoa.’ Eu não digo isso. [<a href="#N1">1</a>] <a
name=R1></a>Mas Timbaruka, [se alguém pensa,] ‘A sensação é uma coisa, aquele
que sente é outra,’ [então ele afirma] a respeito de alguém afligido pela
sensação: ‘O prazer e a dor são criados pelos outros.’ Eu também não digo isso.
[<a href="#N2">2</a>] <a name=R2></a>Evitando esses dois extremos o Tathagata
ensina o Dhamma pelo meio: Da ignorância como condição, as formações volitivas [surgem].
Das formações volitivas como condição, a consciência. Da consciência como condição, a
mentalidade-materialidade (nome e forma). Da mentalidade-materialidade (nome e forma) como condição, as seis
bases dos sentidos. Das seis bases dos sentidos como condição, o contato. Do
contato como condição, a sensação. Da sensação como condição, o desejo. Do
desejo como condição, o apego. Do apego como condição, o ser/existir. Do
ser/existir como condição, o nascimento. Do nascimento como condição, então o
envelhecimento e morte, tristeza, lamentação, dor, angústia e desespero surgem.
Essa é a origem de toda essa massa de sofrimento. Mas com o desaparecimento e
cessação sem deixar vestígios dessa mesma ignorância cessam as formações volitivas. Da
cessação das formações volitivas cessa a consciência. Da cessação da consciência cessa a
mentalidade-materialidade (nome e forma). Da cessação da mentalidade-materialidade (nome e forma) cessam as
seis bases dos sentidos. Da cessação das seis bases dos sentidos cessa o
contato. Da cessação do contato cessa a sensação. Da cessação da sensação cessa
o desejo. Da cessação do desejo cessa o apego. Da cessação do apego cessa o
ser/existir. Da cessação do ser/existir cessa o nascimento. Da cessação do
nascimento, então o envelhecimento e morte, tristeza, lamentação, dor, angústia
e desespero, tudo cessa. Essa é a cessação de toda essa massa de sofrimento.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isso
foi dito, o errante Timbaruka disse para o Abençoado: “Magnífico, Mestre
Gotama! Magnífico, Mestre Gotama! Mestre Gotama esclareceu o Dhamma de várias
formas, como se tivesse colocado em pé o que estava de cabeça para baixo,
revelasse o que estava escondido, mostrasse o caminho para alguém que estivesse
perdido ou segurasse uma lâmpada no escuro para aqueles que possuem visão pudessem
ver as formas. Eu busco refúgio no Mestre Gotama, no Dhamma e na Sangha dos
bhikkhus. Que o Mestre Gotama me aceite como discípulo leigo que buscou
refúgio para o resto da vida.”<b style='mso-bidi-font-weight:normal'><o:p></o:p></b></span></p>


