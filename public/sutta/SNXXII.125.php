<p class=Normal> Em Savatthi. Então o Venerável Kappa foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse: </p>

<p class=Normal> “Venerável senhor, como é que alguém deve compreender, como é que alguém deve ver para que, em relação a este corpo com a consciência e em relação a todos os sinais externos, a mente esteja livre da fabricação do eu, da fabricação do meu e da presunção, e para que ela transcenda a dualidade e esteja em paz e bem libertada?” </p>

<p class=Normal> “Qualquer forma, Kappa, quer seja do passado, futuro ou presente, interna ou externa; grosseira ou sutil; inferior ou superior, próxima ou distante: toda forma deve ser vista como na verdade é, com correta sabedoria: ‘Isso não é meu, isso não sou eu, isso não é o meu eu.’ </p>

<p class=Normal> “Qualquer sensação ... Qualquer percepção ... Quaisquer formações volitivas ... Qualquer consciência, quer seja do passado, do futuro ou do presente, interna ou externa; grosseira ou sutil; inferior ou superior; próxima ou distante: toda consciência deve ser vista como na verdade é, com correta sabedoria: ‘Isso não é meu, isso não sou eu, isso não é o meu eu.’ </p>

<p class=Normal> “Quando alguém sabe e vê assim, Kappa, então, em relação a este corpo com a consciência e em relação a todos os sinais externos, a mente estará livre da fabricação do eu, da fabricação do meu e da presunção, terá transcendido a dualidade e estará em paz e bem libertada.” </p>

<p class=Normal> Então, o Venerável Kappa, satisfeito e contente com as palavras do Abençoado, levantou do seu assento e depois de homenagear o Abençoado, mantendo-o à sua direita, partiu. Depois, permanecendo só, isolado, diligente, ardente e decidido, em pouco tempo, ele alcançou e permaneceu no objetivo supremo da vida santa, pelo qual membros de um clã deixam a vida em família pela vida santa, tendo conhecido e realizado por si mesmo no aqui e agora. Ele soube: “O nascimento foi destruído, a vida santa foi vivida, o que deveria ser feito foi feito, não há mais vir a ser a nenhum estado.” E assim ele se tornou mais um dos arahants. </p>


