<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
não se envolvam em disputas com as pessoas dizendo: [<a href="#N1">1</a>] <a
name=R1></a>‘Você<i> </i>entende esta doutrina e disciplina? Eu<i> </i>sou
aquele que entende esta doutrina e disciplina. Como poderia você entender esta
doutrina e disciplina? Você está praticando da forma errada. Eu estou
praticando da forma correta. O que deveria ser dito por último você disse
primeiro. O que deveria ser dito primeiro você disse por último. Eu sou
consistente. Você não é. Aquilo que lhe custou tanto tempo para pensar foi
refutado. A sua doutrina foi derrubada. Você está derrotado. Vá e tente
resgatar a sua doutrina, ou liberte-se se puder!’ Por qual razão? Porque,
bhikkhus, esse tipo de conversa não traz benefícios, não pertence aos
fundamentos da vida santa e não conduz ao desencantamento, ao desapego, à
cessação, à paz, ao conhecimento direto, à iluminação, a Nibbana.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando
conversarem, bhikkhus, vocês deveriam conversar sobre: ‘Isto é sofrimento’;
vocês deveriam conversar sobre: ‘Esta é a origem do sofrimento’; vocês deveriam
conversar sobre: ‘Esta é a cessação do sofrimento’; vocês deveriam conversar
sobre: ‘Este é o caminho que conduz à cessação do sofrimento.’ Por qual razão?
Porque, bhikkhus, esse tipo de conversa traz benefícios, pertence aos
fundamentos da vida santa e conduz ao desencantamento, ao desapego, à cessação,
à paz, ao conhecimento direto, à iluminação, a Nibbana.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
bhikkhus, um esforço deve ser feito para compreender: ‘Isto é sofrimento.’ <a
name=R2></a>Um esforço deve ser feito para compreender: ‘Esta é a origem do
sofrimento.’ Um esforço deve ser feito para compreender: ‘Esta é a cessação do
sofrimento.’ Um esforço deve ser feito para compreender: ‘Este é o caminho que
conduz à cessação do sofrimento.’”<o:p></o:p></span></p>


