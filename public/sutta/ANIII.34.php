<p class=Normal> "Bhikkhus, há três causas para a origem de kamma. Quais três? A cobiça é uma causa para a origem de kamma; a raiva é uma causa para a origem de kamma; a delusão é uma causa para a origem de kamma. </p>

<p class=Normal> (1) "Qualquer kamma, bhikkhus, criado através da cobiça, nascido da cobiça, causado pela cobiça, originado da cobiça, amadurece onde quer que renasça o ser/existir. Onde quer que o kamma amadureça, é ali que o resultado será experimentado, quer seja nesta vida, ou no próximo renascimento, ou em alguma ocasião subsequente. [<a href="#N1">1</a>]  </p>

<p class=Normal><a name=R1></a> (2) "Qualquer kamma, bhikkhus, criado através da raiva, nascido da raiva, causado pela raiva, originado da raiva, amadurece onde quer que renasça o ser/existir. Onde quer que o kamma amadureça, é ali que o resultado será experimentado, quer seja nesta vida, ou no próximo renascimento, ou em alguma ocasião subsequente. </p>

<p class=Normal> (3) "Qualquer kamma, bhikkhus, criado através da delusão, nascido da delusão, causado pela delusão, originado da delusão, amadurece onde quer que renasça o ser/existir. Onde quer que o kamma amadureça, é ali que o resultado será experimentado, quer seja nesta vida, ou no próximo renascimento, ou em alguma ocasião subsequente. </p>

<p class=Normal>"Suponham, bhikkhus, sementes que estejam intactas, não estejam estragadas, não tenham sido danificadas pelo vento e pelo calor do sol, férteis, bem preservadas e bem plantadas em um solo bem preparado num bom campo que tenha recebido chuva adequada: dessa forma, essas sementes germinariam, cresceriam e amadureceriam. Do mesmo modo, qualquer kamma criado através da cobiça ... raiva ... delusão, nascido da delusão, causado pela delusão, originado da delusão, amadurece onde quer que renasça o ser/existir. Onde quer que o kamma amadureça, é ali que o resultado será experimentado, quer seja nesta vida, ou no próximo renascimento, ou em alguma ocasião subsequente. </p>

<p class=Normal>"Essas são as três causas para a origem de kamma. </p>

<p class=Normal>"Bhikkhus, há essas outras três causas para a origem de kamma. Quais três? A não-cobiça é uma causa para a origem de kamma; a não-raiva é uma causa para a origem de kamma; a não-delusão é uma causa para a origem de kamma. </p>

<p class=Normal> (1) "Qualquer kamma, bhikkhus, criado através da não-cobiça, nascido da não-cobiça, causado pela não-cobiça, originado da não-cobiça, é abandonado quando a cobiça desaparece; é cortado pela raiz, feito como com um tronco de palmeira, eliminado de tal forma que não mais estará sujeito a um futuro surgimento. [<a href="#N2">2</a>] </p>

<p class=Normal><a name=R2></a> (2) "Qualquer kamma, bhikkhus, criado através da não-raiva, nascido da não-raiva, causado pela não-raiva, originado da não-raiva, é abandonado quando a raiva desaparece; é cortado pela raiz, feito como com um tronco de palmeira, eliminado de tal forma que não mais estará sujeito a um futuro surgimento. </p>

<p class=Normal> (3) "Qualquer kamma, bhikkhus, criado através da não-delusão, nascido da não-delusão, causado pela não-delusão, originado da não-delusão, é abandonado quando a delusão desaparece; é cortado pela raiz, feito como com um tronco de palmeira, eliminado de tal forma que não mais estará sujeito a um futuro surgimento. </p>

<p class=Normal>"Suponham, bhikkhus, sementes que estejam intactas, não estejam estragadas, não tenham sido danificadas pelo vento e pelo calor do sol, férteis, bem preservadas. Então um homem as queimaria no fogo, as reduziria a cinzas e espalharia as cinzas num vento forte ou deixaria que elas fossem levadas pela forte correnteza de um rio. Dessa forma, essas sementes estariam cortadas pela raiz, feitas como com um tronco de palmeira, eliminadas de tal forma que não mais estariam sujeitas a um futuro surgimento. Do mesmo modo, qualquer kamma criado através da não-cobiça ... não-raiva ... não-delusão, nascido da não-delusão, causado pela não-delusão, originado da não-delusão, é abandonado quando a delusão desaparece; é cortado pela raiz, feito como com um tronco de palmeira, eliminado de tal forma que não mais estará sujeito a um futuro surgimento. </p>

<p class=Normal>"Essas, bhikkhus, são as três causas para a origem de kamma." </p>

<p class=Normal>Qualquer kamma que uma pessoa ignorante tenha criado,<br>
nascido da cobiça, raiva e delusão,<br> 
seja aquilo que foi criado, pouco ou muito,<br>
é para ser experimentado aqui mesmo:<br>
não há nenhum outro lugar. [<a href="#N3">3</a>] </p>

<p class=Normal>Uma pessoa sábia deve portanto abandonar<br>
qualquer ação nascida da cobiça, raiva e delusão.<br>
Um bhikkhu que faça nascer o conhecimento,<br>
deixa para trás todas destinações miseráveis.</p>


