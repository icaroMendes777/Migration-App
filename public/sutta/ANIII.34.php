<p class=Normal style='margin-left:.75in'>
<p class=Normal><b>Notas:</b></p>

<p class=Normal><a name=N1></a> [1] Não há concordância nas versões em pali preservadas no Sri Lanka, Birmânia e da PTS. Alguns estudiosos argumentam que levando em conta essas discordâncias o sutta na verdade apenas apresenta duas possibilidades: que o kamma pode frutificar "nesta vida ou na próxima". Bhikkhu Bodhi optou por adotar a explicação dada nos comentários que os frutos de kamma são para serem experimentados "nesta vida, no próximo renascimento ou numa existência subsequente." Essa foi também a tradução para o português. [<a href="#R1">Retorna</a>]</p>

<p class=Normal><a name=N2></a> [2] Bhikkhu Bodhi explica que um arahant não cria mais kamma visto que a cobiça, raiva e delusão foram extintas. No entanto ele pode experimentar os frutos de kamma passados. Mas como não haverá o renascimento, todos os frutos de kamma que não tenham dado resultados serão eliminados. </p>

<p class=Normal>Ajaan Brahmali comenta: "Tem que haver uma distinção entre 'não-cobiça' (<i>alobha</i>) e a situação em que a cobiça desapareceu (<i>lobhe vigata</i>). O primeiro caso deve se referir à motivação por trás de uma determinada ação, o segundo ao completo desenraizamento da cobiça alcançado apenas por aquele que não retorna e pelo arahant. Apenas levando em conta essa distinção é que o enunciado do sutta faz sentido." [<a href="#R2">Retorna</a>]</p>

<p class=Normal><a name=N3></a> [3] "Experimentado aqui mesmo: não há nenhum outro lugar", significa que o fruto do kamma será experimentado nesta mesma vida pela própria pessoa e não por alguma outra pessoa.</p>

<p class=Normal>Nota do tradutor: Estes versos indicam que o fruto do kamma será experimentado nesta mesma vida o que contradiz a nota 1 acima.</p> 

