<p class=Normal><a name=N13></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[13] O que deve ser conhecido,<span style='mso-bidi-font-weight:bold'> (<i>abhiññeyya</i>),
são as Quatro Nobres Verdades, o</span> que deve ser desenvolvido, <span
style='mso-bidi-font-weight:bold'><span style='mso-spacerun:yes'> </span>(<i>bhavetabba</i>),
é o Nobre Caminho Óctuplo, e o</span> que deve ser abandonado,<span
style='mso-bidi-font-weight:bold'> (<i>pahatabba</i>), são as impurezas encabeçadas
pela cobiça. Aqui o contexto exige que a palavra “Buda” seja compreendida com o
sentido específico de Um Perfeitamente Iluminado, (<i>sammasambuddha</i>). [<a
href="#R13">Retorna</a>]<o:p></o:p></span></span></p>


<p class=Normal><a name=N14></a><span lang=PT-BR style='mso-ansi-language:
