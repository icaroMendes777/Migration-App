<p class=Normal><a name=N5></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[5] MA explica que o Buda realizou este feito
depois de primeiro se assegurar que o mestre de Uttara, Brahmayu, tinha o
potencial para alcançar o fruto do não retorno, e que a realização desse fruto
dependia da dissipação das dúvidas de Uttara. [<a href="#R5">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[6] As sete são: a parte de trás dos quatro
membros, os dois ombros e o tronco. [<a href="#R6">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N7></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[7] <i>Rasaggasaggi</i>. O <i>Lakkhana Sutta</i>
explica (<a href="DN30.php#T_2_7">DN 30.2.7</a>): “Qualquer coisa que ele toque
com a ponta da língua ele experimenta na sua garganta e o sabor se dispersa por
toda parte.” É difícil, no entanto, entender como essa qualidade pode ser considerada
uma característica física ou como isto poderia ser percebido pelos outros. [<a
href="#R7">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N8></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[8] Esta marca, <i>unhisa</i>, explica a
protuberância que em geral é vista no topo da cabeça das imagens do Buda. [<a
href="#R8">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N9></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[9] Esta é uma reflexão padrão sobre o uso
