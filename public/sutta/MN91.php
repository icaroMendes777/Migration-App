href="#R9">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N10></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[10] A benção, (<i>anumodana</i>), é um breve
discurso depois da refeição, instruindo os doadores sobre algum aspecto do
Dhamma e expressando o desejo de que o kamma meritório traga muitos frutos. [<a
href="#R10">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N11></a><span lang=PT-BR style='mso-ansi-language:
