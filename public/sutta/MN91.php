descrevi. As excelentes qualidades do Mestre Gotama são como a grande terra e o
grande oceano; expostas em detalhe estas são infinitas e imensuráveis, como o
espaço.” [<a href="#R11">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N12></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[12] A palavra em Pali para língua, <i>jivha</i>,
