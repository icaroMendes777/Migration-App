<p class=Normal> "Bhikkhus, esta vida santa é vivida sem discípulos e sem um mestre. Um bhikkhu que tem discípulos e um mestre permanece no sofrimento, não em conforto. Um bhikkhu que não tem discípulos e não tem um mestre permanece feliz, em conforto. </p>

<p class=Normal> "Bhikkhus, como um bhikkhu que tem discípulos e um mestre permanece no sofrimento, não em conforto? Aqui, bhikkhus, quando um bhikkhu viu uma forma com o olho, surgem nele estados ruins e prejudiciais, memórias e intenções conectadas com os grilhões. Isso permanece no seu interior. Visto que esses estados ruins e prejudiciais permanecem no seu interior, ele é chamado aquele 'que tem discípulos'. Eles o atacam. Visto que esses estados ruins e prejudiciais o atacam, ele é chamado aquele 'que tem um mestre'. </p>

<p class=Normal> "Além disso, quando um bhikkhu ouve um som com o ouvido ... conscientiza um objeto mental com a mente ... ele é chamado aquele 'que tem um mestre'.

"Dessa forma um bhikkhu que tem discípulos e um mestre permanece no sofrimento, não em conforto. </p>

<p class=Normal> "Bhikkhus, como um bhikkhu não tem discípulos e não tem um mestre permanece feliz, em conforto? Aqui, bhikkhus, quando um bhikkhu viu uma forma com o olho, não surgem nele estados ruins e prejudiciais, memórias e intenções conectadas com os grilhões. Isso não permanece no seu interior. Visto que esses estados ruins e prejudiciais não permanecem no seu interior, ele é chamado aquele 'que não tem discípulos'. Eles não o atacam. Visto que esses estados ruins e prejudiciais não o atacam, ele é chamado aquele 'que não tem um mestre'. </p>

<p class=Normal> "Além disso, quando um bhikkhu ouve um som com o ouvido ... conscientiza um objeto mental com a mente ... ele é chamado aquele 'que não tem um mestre'. </p>

<p class=Normal> "Dessa forma um bhikkhu que não tem discípulos e não tem um mestre permanece feliz, em conforto. </p>

<p class=Normal> "Bhikkhus, esta vida santa é vivida sem discípulos e sem um mestre. Um bhikkhu que tem discípulos e um mestre permanece no sofrimento, não em conforto. Um bhikkhu que não tem discípulos e não tem um mestre permanece feliz, em conforto." </p>

