<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
suponham que exista uma hospedaria. Pessoas vindas do leste, do oeste, do norte
ou do sul ali se hospedem. Pessoas da casta dos nobres, da casta dos Brâmanes, da casta dos
comerciantes e da casta dos trabalhadores ali se hospedem. Da mesma maneira, bhikkhus,
neste corpo surgem vários tipos de sensações: surgem sensações prazerosas,
surgem sensações dolorosas e surgem sensações nem dolorosas, nem prazerosas;
sensações mundanas que são prazerosas, dolorosas ou nem dolorosas, nem
prazerosas; sensações não mundanas que são prazerosas, dolorosas ou nem
dolorosas, nem prazerosas.”<a name=R1></a> [<a href="#N1">1</a>] <o:p></o:p></span></p>


