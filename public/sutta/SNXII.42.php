<p class=Normal> Em Savathi. Então Um certo número de bhikkhus foram até o Abençoado e ao chegar depois de cumprimentá-lo sentaram a um lado. Tendo sentado, o Abençoado lhes disse: </p>

<p class=Normal> “Quando, num nobre discípulo, as cinco formas de medo e animosidade estão silenciadas; quando ele possui os quatro fatores para entrar na correnteza; e quando, através da sabedoria, ele viu claramente e penetrou completamente o nobre método, então se ele quiser poderá afirmar acerca de si mesmo: ‘O inferno foi destruído, os ventres animais foram destruídos; o estado dos fantasmas famintos foi destruído; os estados de privação, os destinos infelizes foram destruídos! Eu entrei na correnteza, não mais destinado aos mundos inferiores, com o destino fixo, tendo a iluminação como destino!’ </p>

<p class=Normal> “Agora, quais são as cinco formas de medo e animosidade que são silenciadas? </p>

<p class=Normal> “Quando alguém tira a vida, então, tendo o tirar a vida como condição, ele produz medo e animosidade no aqui e agora, produz medo e animosidade em vidas futuras, experimenta concomitantes mentais de dor e tristeza; mas ao abster-se de tirar a vida, ele nem produz medo e animosidade no aqui e agora, nem produz medo e animosidade em vidas futuras, nem experimenta concomitantes mentais de dor e tristeza: pois naquele que se abstém de tirar a vida, aquele medo e animosidade é dessa forma silenciado. </p>

<p class=Normal> “Quando uma pessoa rouba … pratica o sexo impróprio … diz mentiras …</p>

<p class=Normal> “Quando alguém bebe vinho, álcool e outros embriagantes que causam a negligência, então, tendo o beber vinho, álcool e outros embriagantes que causam a negligência como condição, ele produz medo e animosidade no aqui e agora, produz medo e animosidade em vidas futuras, experimenta concomitantes mentais de dor e tristeza mas ao abster-se de beber vinho, álcool e outros embriagantes que causam a negligência, ele nem produz medo e animosidade no aqui e agora, nem produz medo e animosidade em vidas futuras, nem experimenta concomitantes mentais de dor e tristeza: pois naquele que se abstém de beber vinho, álcool e outros embriagantes que causam a negligência, aquele medo e animosidade é dessa forma silenciado. </p>

<p class=Normal> “Essas são as cinco formas de medo e animosidade que são silenciadas. </p>

<p class=Normal> “E quais são os quatro fatores para entrar na correnteza que ele possui? </p>

<p class=Normal> “É o caso em que o nobre discípulo possui perfeita claridade, serenidade e confiança no Abençoado: ‘De fato, o Abençoado é sublime, digno, perfeitamente iluminado, consumado no verdadeiro conhecimento e conduta, bem-aventurado, conhecedor dos mundos, um líder insuperável de pessoas preparadas para serem treinadas, mestre de devas e humanos, iluminado, sublime.’ </p>

<p class=Normal> “Ele possui perfeita claridade, serenidade e confiança no Dhamma: ‘O Dhamma é bem proclamado pelo Abençoado, visível no aqui e agora, com efeito imediato, que convida ao exame, que conduz para adiante, para ser experimentado pelos sábios por eles mesmos.’ </p>

<p class=Normal> “Ele possui perfeita claridade, serenidade e confiança na Sangha: ‘A Sangha dos discípulos do Abençoado pratica o bom caminho, pratica o caminho reto, pratica o caminho verdadeiro, pratica o caminho adequado, isto é, os quatro pares de pessoas, os oito tipos de indivíduos; esta Sangha dos discípulos do Abençoado é merecedora de dádivas, merecedora de hospitalidade, merecedora de oferendas, merecedora de saudações com reverência, um campo inigualável de mérito para o mundo.’ </p>

<p class=Normal> “Ele possui as virtudes apreciadas pelos nobres – intactas, não-laceradas, imaculadas, não-matizadas, libertadoras, elogiadas pelos sábios, desapegadas, que conduzem à concentração. </p>

<p class=Normal> “Esses são os quatro fatores para entrar na correnteza que ele possui. </p>

<p class=Normal>"E qual é o nobre método que ele viu claramente e penetrou completamente através da sabedoria? É o caso em que um nobre discípulo observa: </p>

<p class=Normal>Quando existe isso, aquilo existe. <br>
Com o surgimento disso, aquilo surge. <br>
Quando não existe isso, aquilo também não existe. <br>
Com a cessação disto, aquilo cessa. </p>

<p class=Normal>"Em outras palavras: </p>

<p class=Normal>"Da ignorância como condição, as formações volitivas [surgem]. <br>
Das formações volitivas como condição, a consciência. <br>
Da consciência como condição, a mentalidade-materialidade (nome e forma). <br>
Da mentalidade-materialidade (nome e forma) como condição, as seis bases dos sentidos. <br>
Das seis bases dos sentidos como condição, o contato. <br>
Do contato como condição, as sensações. <br>
Das sensações como condição, o desejo. <br>
Do desejo como condição surge o apego. <br>
Do apego como condição, o ser/existir. <br>
Do ser/existir como condição, o nascimento. <br>
Do nascimento como condição, envelhecimento e morte, tristeza, lamentação, dor, angústia e desespero. <br>
Essa é a origem dessa massa de sofrimento. </p>

<p class=Normal>"Agora do desaparecimento e cessação sem deixar vestígios dessa mesma ignorância cessam as formações volitivas. <br>
Da cessação das formações volitivas cessa a consciência. <br>
Da cessação da consciência cessa a mentalidade-materialidade (nome e forma). <br>
Da cessação da mentalidade-materialidade (nome e forma) cessam as seis bases dos sentidos. <br>
Da cessação das seis bases dos sentidos cessa o contato. <br>
Da cessação do contato cessa a sensação. <br>
Da cessação da sensação cessa o desejo.<br>
Da cessação do desejo cessa o apego. <br>
Da cessação do apego cessa o ser/existir. <br>
Da cessação do ser/existir cessa o nascimento. <br>
Da cessação do nascimento, então, envelhecimento e morte, tristeza, lamentação, dor, angústia e desespero, tudo cessa. <br>
Essa é a cessação dessa massa de sofrimento. </p>

<p class=Normal>"Esse é o nobre método que ele viu claramente e penetrou completamente através da sabedoria.” </p>

<p class=Normal> “Quando, num nobre discípulo, as cinco formas de medo e animosidade estão silenciadas; quando ele possui os quatro fatores para entrar na correnteza; e quando, através da sabedoria ele viu claramente e penetrou completamente o nobre método, então se ele quiser poderá afirmar acerca de si mesmo: ‘O inferno foi destruído, os ventres animais foram destruídos; o estado dos fantasmas famintos foi destruído; os estados de privação, os destinos infelizes foram destruídos! Eu entrei na correnteza, não mais destinado aos mundos inferiores, com o destino fixo, tendo a iluminação como destino! </p>

