<p class=Normal><a name=R1></a> Em Savatthi. "Bhikkhus, quando alguém permanece contemplando a gratificação nas coisas que agrilhoam, a mentalidade-materialidade (nome e forma) é estabelecida. [<a href="#N1">1</a>] Com a mentalidade-materialidade (nome e forma) como condição, as seis bases dos sentidos surgem ... Essa é a origem de toda essa massa de sofrimento. </p>

<p class=Normal> "Suponham, bhikkhus, que houvesse uma grande árvore e todas as suas raízes espalhadas pela terra enviassem a seiva para cima. Sustentada por essa seiva, alimentada por ela, essa grande árvore se manteria em pé por muito tempo. Do mesmo modo, quando alguém permanece contemplando a gratificação nas coisas que agrilhoam, a mentalidade-materialidade (nome e forma) é estabelecida. Com a mentalidade-materialidade (nome e forma) como condição, as seis bases dos sentidos surgem ... Essa é a origem de toda essa massa de sofrimento. </p>

<p class=Normal> "Bhikkhus, quando alguém permanece contemplando o perigo nas coisas que agrilhoam, a mentalidade-materialidade (nome e forma) não é estabelecida. Com a cessação da mentalidade-materialidade (nome e forma), as seis bases dos sentidos cessam; com a cessação das seis bases dos sentidos ... Essa é a cessação de toda essa massa de sofrimento. </p>

<p class=Normal> "Suponham, bhikkhus, que houvesse uma grande árvore. Então um homem viesse trazendo uma pá e um cesto. Ele cortaria a árvore pela raiz, cavaria e arrancaria todas as raízes, até mesmo as mais delgadas. Ele cortaria a árvore em pedaços, partiria os pedaços em pedaços menores e os reduziria a lascas. Depois ele secaria as lascas ao vento e ao sol, queimaria essas lascas no fogo e recolheria as cinzas. Tendo feito isso, ele deixaria as cinzas serem levadas pelo vento ou as jogaria na correnteza de um rio. Assim, aquela grande árvore teria sido cortada pela raiz, feita como um tronco de palmeira, eliminada de tal forma que não estará mais sujeita a um a um futuro surgimento. Do mesmo modo, quando alguém permanece contemplando o perigo nas coisas que agrilhoam, a mentalidade-materialidade (nome e forma) não é estabelecida. Com a cessação da mentalidade-materialidade (nome e forma), as seis bases dos sentidos cessam; com a cessação das seis bases dos sentidos ... Essa é a cessação de toda essa massa de sofrimento."</p>

