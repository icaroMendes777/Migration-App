<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, se os errantes de outras seitas perguntarem para vocês: ‘Por
qual propósito, amigos, a vida santa é vivida sob o contemplativo Gotama?’ –
sendo assim perguntados, vocês deveriam responder desta forma: ‘É, amigos, para
o abandono dos grilhões ... para o desenraizamento das tendências subjacentes
... para a completa compreensão do ciclo ... para a destruição das impurezas
... para a realização do fruto do verdadeiro conhecimento e libertação ... pelo
conhecimento e visão ... pelo Nibbana final sem apego, que a vida santa é
vivida sob o Abençoado.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
bhikkhus, se os errantes de outras seitas perguntarem para vocês: ‘Mas, amigos,
há um caminho, há um meio para alcançar o Nibbana final sem apego?’ – sendo
assim preguntados, vocês deveriam responder desta forma: ‘Há um caminho,
amigos, há um meio para alcançar o Nibbana final sem apego.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é esse caminho, qual é esse meio para alcançar o Nibbana final sem
apego? É este Nobre Caminho Óctuplo; isto é, entendimento correto ...
concentração correta. Esse é o caminho, esse é o meio para alcançar o Nibbana
final sem apego.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sendo
assim perguntados, bhikkhus, é dessa forma que vocês deveriam responder aos
errantes de outras seitas.”<o:p></o:p></span></p>

