<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável senhor, o que
são as sensações? Qual é a origem das sensações? Qual é a cessação das
sensações? Qual é o caminho que conduz à cessação das sensações? Qual é a
gratificação das sensações? Qual é o perigo das sensações? Qual é a escapatória
das sensações?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ananda, há esses três
tipos de sensações: prazerosas, dolorosas e nem dolorosas, nem prazerosas. Com
o surgimento do contato surgem as sensações. Cessando o contato, cessam as
sensações. Este nobre caminho óctuplo é o caminho que conduz à cessação das
sensações, isto é: entendimento correto, pensamento correto, linguagem correta,
ação correta, modo de vida correto, esforço correto, atenção plena correta e
concentração correta. O prazer e a alegria que surgem na dependência das
sensações: essa é a gratificação das sensações. As sensações são impermanentes,
sofrimento e sujeitas à mudança: esse é o perigo das sensações. A remoção e o
abandono do desejo e cobiça pelas sensações: essa é a escapatória das
sensações. <o:p></o:p></span></p>

<p class=Normal>“Além disso, Ananda, eu também ensinei a cessação sucessiva das formações. Naquele que alcançou o primeiro
jhana, a linguagem cessa. Naquele que alcançou o segundo jhana, o pensamento
aplicado e o pensamento sustentado cessam. Naquele que alcançou o terceiro
jhana, o êxtase cessa. Naquele que alcançou o quarto jhana, a inspiração e a
expiração cessam. Naquele que alcançou a base do espaço infinito, a percepção
da forma cessa. Naquele que alcançou a base da consciência infinita, a
percepção da base do espaço infinito cessa. Naquele que alcançou a base do
nada, a percepção da base da consciência infinita cessa. Naquele que alcançou a
base da nem percepção, nem não percepção, a percepção da base do nada cessa.
Naquele que alcançou a cessação da percepção e sensação, a percepção e sensação
cessam. No bhikkhu que está livre das impurezas a cobiça cessa, a raiva cessa,
a delusão cessa.”<span style='mso-ansi-language:
PT-BR'> <span lang=PT-BR><o:p></o:p></span></span></p>


