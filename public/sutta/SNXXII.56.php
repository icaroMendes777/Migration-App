<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em <a
name=savatthi>Savatthi</a>. “Bhikkhus, existem esses cinco agregados influenciados
pelo apego. Quais cinco? A forma como um agregado influenciado pelo apego, a
sensação como um agregado influenciado pelo apego, a percepção como um agregado
influenciado pelo apego, as formações volitivas como um agregado influenciado pelo apego,
a consciência como um agregado influenciado pelo apego. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>‘Agora, enquanto eu não
obtive o conhecimento direto das fases desses cinco agregados influenciados
pelo apego, eu não reivindiquei ter despertado para a insuperável perfeita
iluminação neste mundo com os seus devas, maras e brahmas,
esta população com seus contemplativos e brâmanes, seus príncipes e povo. Mas,
quando obtive o conhecimento direto de como na verdade são, então reivindiquei
ter despertado para a insuperável perfeita iluminação neste mundo com ... seus
príncipes e povo. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como, bhikkhus, são as
quatro fases? Eu obtive o conhecimento direto da forma ... da origem da forma ... da
cessação da forma ... do caminho da prática que conduz à cessação da forma. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu obtive o conhecimento
direto da sensação… <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu obtive o conhecimento
direto da<span style="mso-spacerun: yes">  </span>percepção... <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu obtive o conhecimento
direto das formações volitivas… <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu obtive o conhecimento
direto da consciência ... da origem da consciência ... da cessação da consciência ... do
caminho da prática que conduz à cessação da consciência. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que,
bhikkhus, é forma? Os quatro grandes elementos, (o elemento terra, o elemento
água, o elemento fogo e o elemento ar), e a forma deles derivada: a isso se
denomina forma. Com o surgimento do alimento comida, surge a forma. Cessando o
alimento comida, cessa a forma. E exatamente este nobre caminho óctuplo é o
caminho da prática que conduz à cessação da forma, isto é, entendimento
correto, pensamento correto, linguagem correta, ação correta, modo de vida
correto, esforço correto, atenção plena correta, concentração correta.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Todos os
brâmanes ou contemplativos que, conhecendo assim, de modo direto, a forma,
conhecendo assim, de modo direto, a origem da forma, conhecendo assim, de modo direto,
a cessação da forma, conhecendo assim, de modo direto o caminho da prática que
conduz à cessação da forma, estiverem praticando para o desencantamento,
desapego, cessação em relação à forma, eles estarão praticando do modo correto.
Aqueles que praticam do modo correto estão apoiados com firmeza neste Dhamma e
Disciplina. E todos os brâmanes ou contemplativos que assim conheceram de modo
direto a forma, conheceram de modo direto a origem da forma, conheceram de modo
direto a cessação da forma, conheceram de modo direto o caminho da prática que
conduz à cessação da forma, estão, por meio do desencantamento, desapego,
cessação em relação à forma, libertados através do desapego, eles estão bem
libertados. Aqueles que estão bem libertados estão totalmente consumados. E com
relação àqueles que estão totalmente consumados, não existe ciclo para
descrevê-los.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que,
bhikkhus, é a sensação? Esses seis grupos de sensações – sensação que nasce do
contato no olho, sensação que nasce do contato no ouvido, sensação que nasce do
contato no nariz, sensação que nasce do contato na língua, sensação que nasce
do contato no corpo, sensação que nasce do contato na mente: a isso se denomina
sensação. Com o surgimento do contato, surge a sensação. Cessando o contato,
cessa a sensação. E exatamente este nobre caminho óctuplo é o caminho da
prática que conduz à cessação da sensação ...<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que,
bhikkhus, é a percepção? Esses seis grupos de percepções – percepção da forma,
percepção do som, percepção do aroma, percepção do sabor, percepção dos
tangíveis, percepção dos objetos mentais: a isso se denomina percepção. Com o
surgimento do contato, surge a percepção. Cessando o contato, cessa a
percepção. E exatamente este nobre caminho óctuplo é o caminho da prática que conduz
à cessação da percepção ...<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que,
bhikkhus, são as formações volitivas? Esses seis grupos de volições (ou intenções) – volições
relacionadas às formas, volições relacionadas aos sons, volições relacionadas aos
aromas, volições relacionadas aos sabores, volições relacionadas aos tangíveis,
volições relacionadas aos objetos mentais: a isso se denomina formações volitivas. Com o surgimento do contato,
surgem as formações volitivas. Cessando o contato, cessam as formações volitivas. E exatamente este
nobre caminho óctuplo é o caminho da prática que conduz à cessação das
formações volitivas ...<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que,
bhikkhus, é a consciência? Esses seis grupos de consciência - consciência no
olho, consciência no ouvido, consciência no nariz, consciência na língua,
consciência no corpo, consciência na mente: a isso se denomina consciência. Com
o surgimento da mentalidade-materialidade (nome e forma), surge a consciência. Cessando a
mentalidade-materialidade (nome e forma), cessa a consciência. E exatamente este nobre caminho
óctuplo é o caminho da prática que conduz à cessação da consciência, isto é, entendimento
correto, pensamento correto, linguagem correta, ação correta, modo de vida
correto, esforço correto, atenção plena correta, concentração correta.&nbsp;<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Todos os brâmanes ou
contemplativos que, conhecendo assim, de modo direto, a consciência, conhecendo
assim, de modo direto, a origem da consciência, conhecendo assim, de modo
direto, a cessação da consciência, conhecendo assim, de modo direto o caminho
da prática que conduz à cessação da consciência, estiverem praticando para o
desencantamento, desapego, cessação em relação à consciência, eles estarão
praticando do modo correto. Aqueles que praticam do modo correto estão apoiados
com firmeza neste Dhamma e Disciplina. E todos os brâmanes ou contemplativos
que assim conheceram de modo direto a consciência, conheceram de modo direto a
origem da consciência, conheceram de modo direto a cessação da consciência,
conheceram de modo direto o caminho da prática que conduz à cessação da
consciência, estão, por meio do desencantamento, desapego, cessação em relação
à consciência, libertados através do desapego, eles estão bem libertados.
Aqueles que estão bem libertados estão totalmente consumados. E com relação
àqueles que estão totalmente consumados, não existe ciclo para descrevê-los.<o:p></o:p></span></p>


