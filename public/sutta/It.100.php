<p class=Normal><a name=R1></a> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: "Bhikkhus, eu sou um brâmane sempre acessível aos apelos [<a href="#N1">1</a>], mão aberta [<a href="#N2">2</a>], que suporta o último corpo, médico e cirurgião insuperável [<a href="#N3">3</a>]. Vocês são os meus legítimos filhos, nascidos da minha boca [<a href="#N4">4</a>], nascidos do Dhamma, formados pelo Dhamma, herdeiros do Dhamma, não os herdeiros de coisas materiais.</p>

<p class=Normal> "Bhikkhus, há duas formas de generosidade: a generosidade com coisas materiais e a generosidade com o Dhamma. Dessas duas formas de generosidade, esta é a principal, isto é, a generosidade com o Dhamma. Há duas formas de compartilhar ... duas formas de ajudar  ... duas formas de dar oferendas: a oferenda de coisas materiais e a oferenda do Dhamma. [<a href="#N5">5</a>] Dessas duas formas de oferenda, esta é a principal, isto é, a oferenda do Dhamma."</p>

<p class=Normal style='margin-left:.75in'> O Tathagata ofereceu o Dhamma, <br>
desapegado, compassivo com todos os seres; <br>
seres vivos o reverenciam, <br>
aquele que foi mais além, líder de devas e humanos.</p>



