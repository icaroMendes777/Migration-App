<p class=Normal> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: "Suponham, bhikkhus, alguém que estivesse sendo levado pela correnteza de um rio que parecia ser agradável e prazenteira. Mas ao vê-lo, um homem com boa visão em pé na margem do rio o chamaria: 'Olá bom homem! Embora você esteja sendo levado pela correnteza desse rio que parece ser agradável e prazenteira, rio abaixo há uma lagoa com ondas fortes e redemoinhos, com monstros e demônios. Chegando nessa lagoa você irá morrer ou terá um sofrimento igual à morte.' Então, bhikkhus, ao ouvir as palavras daquele homem ele lutaria contra a correnteza com as mãos e os pés.</p>

<p class=Normal> “Eu citei este símile, bhikkhus, para transmitir uma idéia. A idéia é a seguinte: 'a correnteza do rio' representa o desejo. 'Parece ser agradável e prazenteira' representa as seis bases dos sentidos internas. 'A lagoa rio abaixo' representa os cinco grilhões inferiores. As 'ondas fortes' representam a raiva e a frustração. Os 'redemoinhos' representam os cinco elementos do prazer sensual. Os 'monstros e demônios' representam as mulheres. 'Contra a correnteza' representa a renúncia. 'Lutar com as mãos e os pés' representa o estímulo da energia. O 'homem com boa visão em pé na margem do rio' representa o Tathagata, o arahant, plenamente iluminado."</p>

<p class=Normal style='margin-left:.75in'> Desejando a futura segurança contra o cativeiro<br>
ele deve abandonar o desejo sensual<br>
não importa quão doloroso seja. <br>
Compreendendo corretamente através da sabedoria, <br>
possuindo uma mente bem emancipada, <br>
ele poderá alcançar a libertação passo a passo. <br>
Quem é um mestre no conhecimento, <br>
que viveu a vida santa, <br>
é chamado aquele que chegou ao fim do mundo, <br>
aquele que alcançou a outra margem.</p>



