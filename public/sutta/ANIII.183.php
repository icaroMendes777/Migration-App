<p class=Normal> (310) "Bhikkhus, para o conhecimento direto da cobiça, três coisas devem ser desenvolvidas. Quais três? Concentração no vazio, concentração sem sinais, concentração no nada (sem desejos). Para o conhecimento direto da cobiça, essas três coisas devem ser desenvolvidas." </p>



