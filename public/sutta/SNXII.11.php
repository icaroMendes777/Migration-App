<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim ouvi.
Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no Parque
de Anathapindika. Lá ele se dirigiu aos monges desta forma: “Bhikkhus” –
“Venerável Senhor,” eles responderam. O Abençoado disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Bhikkhus,
existem esses quatro tipos de alimentos para a manutenção dos seres que já
nasceram e para o sustento daqueles que estão em busca de um nascimento. Quais
quatro? O alimento comida, grosseira ou sutil, o contato como o segundo, a
volição mental como o terceiro e a consciência como o quarto. Estes são os
quatro tipos de alimento para a manutenção dos seres que já nasceram e para o
sustento daqueles que estão buscando o nascimento.[</span><a href="#N1"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>1</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Agora, bhikkhus, o que esses quatro tipos de alimento possuem como
origem, qual é o seu princípio, do que eles nascem e são produzidos? Esses quatro
tipos de alimento possuem o desejo como origem, desejo como o seu princípio;
eles nascem e são produzidos pelo desejo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E esse
desejo possui o que como origem, qual é o seu princípio, do que ele nasce e é
produzido? O desejo possui a sensação como origem, sensação como o seu
princípio; ele nasce e é produzido pela sensação.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E essa
sensação possui o que como origem...? A sensação possui o contato como origem
... E esse contato possui o que como origem...? O contato possui as seis bases como
origem ... E essas seis bases possuem o que como origem? As seis bases possuem
a mentalidade-materialidade (nome e forma) como origem ... E essa mentalidade-materialidade (nome e forma)
possui o que como origem ...? A mentalidade-materialidade (nome e forma) possui a consciência
como origem ... E essa consciência possui o que como origem...? A consciência
possui as formações volitivas como origem ... E essas formações volitivas possuem o que como
origem, qual é o seu princípio, do que elas nascem e são produzidas? As
formações volitivas possuem a ignorância como sua origem, ignorância como o seu
princípio; elas nascem e são produzidas pela ignorância.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
bhikkhus, da ignorância como condição, as formações volitivas [surgem]. Das formações volitivas 
como condição, a consciência. Da consciência como condição, a
mentalidade-materialidade (nome e forma). Da mentalidade-materialidade (nome e forma) como condição, as seis
bases dos sentidos. Das seis bases dos sentidos como condição, o contato. Do
contato como condição, a sensação. Da sensação como condição, o desejo. Do
desejo como condição, o apego. Do apego como condição, o ser/existir. Do
ser/existir como condição, o nascimento. Do nascimento como condição, então o
envelhecimento e morte, tristeza, lamentação, dor, angústia e desespero surgem.
Assim é a origem de toda essa massa de sofrimento. Mas com o desaparecimento e
cessação sem deixar vestígios dessa mesma ignorância cessam as formações volitivas. Da
cessação das formações volitivas cessa a consciência. Da cessação da consciência cessa a
mentalidade-materialidade (nome e forma). Da cessação da mentalidade-materialidade (nome e forma) cessam as
seis bases dos sentidos. Da cessação das seis bases dos sentidos cessa o
contato. Da cessação do contato cessa a sensação. Da cessação da sensação cessa
o desejo. Da cessação do desejo cessa o apego. Da cessação do apego cessa o
ser/existir. Da cessação do ser/existir cessa o nascimento. Da cessação do
nascimento, então o envelhecimento e morte, tristeza, lamentação, dor, angústia
e desespero, tudo cessa. Essa é a cessação de toda essa massa de sofrimento.”<o:p></o:p></span></p>




