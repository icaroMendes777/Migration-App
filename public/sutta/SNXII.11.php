<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] Os alimentos são condições, (<i>paccaya</i>), pois
as condições são chamadas de alimentos, (<i>ahara</i>), porque elas nutrem (ou
produzem) os seus próprios efeitos. Embora existam outras condições para a
existência, apenas estas quatro são chamadas de alimento porque servem como
condições especiais para o contínuo da vida. O alimento comida é uma condição
especial para o corpo físico daqueles seres que se alimentam de comida. No
corpo mental o contato é uma condição especial para a sensação, a volição
mental para a consciência e a consciência para a mentalidade-materialidade (nome e forma). [<a
