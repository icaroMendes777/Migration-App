<p class=Normal>"Bhikkhus, um cobertor de pêlos é declarado como o pior tipo de vestuário. Um cobertor de pêlos é frio quando faz frio, quente quando faz calor, feio, cheira mal e desconfortável. Da mesma forma, a doutrina de Makkhali é declarada como a pior entre as doutrinas dos vários contemplativos. O oco Makkhali ensina a doutrina e entendimento que 'Não há kamma, não há ação, não há energia.' [<a href="#N1">1</a>]</p>

<p class=Normal><a name=R1></a> (1) "Bhikkhus, os Abençoados, arahants, perfeitamente iluminados do passado ensinaram a doutrina do kamma, da ação, da energia. No entanto o oco Makkhali os contradiz dizendo: 'Não há kamma, não há ação, não há energia.'</p>

<p class=Normal> (2) "Bhikkhus, os Abençoados, arahants, perfeitamente iluminados do futuro ensinarão a doutrina do kamma, da ação, da energia. No entanto o oco Makkhali os contradiz dizendo: 'Não há kamma, não há ação, não há energia.'</p>

<p class=Normal> (3) "Bhikkhus, no presente eu sou o Abençoados, arahant, perfeitamente iluminado, e eu ensino a doutrina do kamma, da ação, da energia. No entanto o oco Makkhali me contradiz dizendo: 'Não há kamma, não há ação, não há energia.'</p>

<p class=Normal>"Tal como uma armadilha colocada na foz de um rio traria muito dano, sofrimento, calamidade e desastre para muitos peixes, assim também, o oco Makkhali é como se fosse uma armadilha para as pessoas, ele surgiu no mundo para o dano, sofrimento, calamidade e desastre para muitos seres." </p>

