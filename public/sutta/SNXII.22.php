<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, possuindo esses dez poderes e esses quatro tipos de
intrepidez o Tathagata reivindica o lugar de líder do rebanho, ruge o seu
rugido de leão nas assembléias e coloca em movimento a roda de Brahma [<a href="#N1">1</a>]<a name="R1"> </a>: ‘Assim é
a forma, essa é a sua origem, essa é a sua cessação; assim é a sensação, essa é
a sua origem, essa é a sua cessação; assim é a percepção, essa é a sua origem,
essa é a sua cessação; assim são as formações volitivas, essa é a sua origem, essa é a
sua cessação; assim é a consciência, essa é a sua origem, essa é a sua
cessação. Portanto, quando existe isso, aquilo existe; com o surgimento disso,
aquilo surge. Quando não existe isso, aquilo também não existe; com a cessação
disto, aquilo cessa. Isto é, da ignorância como condição, as formações volitivas
[surgem]. Das formações volitivas como condição, a consciência. Da consciência como
condição, a mentalidade-materialidade (nome e forma). Da mentalidade-materialidade (nome e forma) como
condição, as seis bases dos sentidos. Das seis bases dos sentidos como
condição, o contato. Do contato como condição, a sensação. Da sensação como
condição, o desejo. Do desejo como condição, o apego. Do apego como condição, o
ser/existir. Do ser/existir como condição, o nascimento. Do nascimento como
condição, então o envelhecimento e morte, tristeza, lamentação, dor, angústia e
desespero surgem. Essa é a origem de toda essa massa de sofrimento. Mas do
desaparecimento e cessação sem deixar vestígios dessa mesma ignorância cessam
as formações volitivas. Da cessação das formações volitivas cessa a consciência. Da cessação da
consciência cessa a mentalidade-materialidade (nome e forma). Da cessação da
mentalidade-materialidade (nome e forma) cessam as seis bases dos sentidos. Da cessação das
seis bases dos sentidos cessa o contato. Da cessação do contato cessa a
sensação. Da cessação da sensação cessa o desejo. Da cessação do desejo cessa o
apego. Da cessação do apego cessa o ser/existir. Da cessação do ser/existir
cessa o nascimento. Da cessação do nascimento, então o envelhecimento e morte,
tristeza, lamentação, dor, angústia e desespero, tudo cessa. Essa é a cessação
de toda essa massa de sofrimento.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
o Dhamma foi assim bem exposto por mim, elucidado, anunciado, revelado, despido
de remendos. Quando, bhikkhus, o Dhamma foi assim bem exposto por mim,
elucidado, anunciado, revelado, despido de remendos, isso é o suficiente para
que o membro de um clã que seguiu a vida santa com base na fé desperte a sua
energia da seguinte forma: ‘Com satisfação eu deixaria a carne e o sangue do meu corpo secar, deixando só pele, tendões e ossos, se eu não 
tiver atingido o que pode ser atingido através da firmeza humana, da energia humana e do esforço humano, 
não haverá nenhum relaxamento na minha energia.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
a pessoa preguiçosa permanece com o sofrimento, manchada pelos estados ruins e
prejudiciais, e substancial é o benefício pessoal que ela negligencia. Mas a
pessoa energética permanece com a felicidade, isolada dos estados ruins e
prejudiciais, e substancial é o benefício pessoal que ela alcança. Não é
através do inferior que o supremo é alcançado; mas, é através do supremo que o
supremo é alcançado. Bhikkhus, esta vida santa é como uma ambrosia; o Mestre
está presente. Portanto, bhikkhus, despertem a sua energia para alcançar aquilo
que ainda não foi alcançado, para lograr aquilo que ainda não foi logrado, para
realizar aquilo que ainda não foi realizado, [com o pensamento]: ‘Dessa forma
esta nossa vida santa não será estéril, mas frutuosa e fértil; e quando usamos
os mantos, comida esmolada, moradia e medicamentos [que nos são oferecidos
pelos outros] essas oferendas que eles nos proporcionam lhes trarão muitos frutos
e benefícios.’ Assim, bhikkhus, vocês deveriam praticar.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Considerando
o seu próprio bem, bhikkhus, é o suficiente para se esforçar pelo objetivo com
diligência; considerando o bem dos outros, é o suficiente para se esforçar pelo
objetivo com diligência; considerando o bem de ambos, é o suficiente para se
esforçar pelo objetivo com diligência.”<b style='mso-bidi-font-weight:normal'><o:p></o:p></b></span></p>

