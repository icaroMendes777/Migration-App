<p class=Normal> “Bhikkhus, uma pessoa comum sem instrução sente sensações prazerosas, sente
sensações dolorosas, sente sensações nem dolorosas, nem prazerosas. Um nobre
discípulo bem instruído também sente sensações prazerosas, sente sensações
dolorosas, sente sensações nem dolorosas, nem prazerosas. Então, bhikkhus, qual
é a variação, qual é a distinção, qual é a diferença que distingue o<span
style="mso-spacerun: yes">  </span>nobre discípulo bem instruído de uma pessoa
comum sem instrução?”<o:p></o:p></span></p>


<p class=Normal> “Venerável senhor,
os nossos ensinamentos têm o Abençoado como origem, como guia e como refúgio.
Seria bom se o Abençoado pudesse explicar o significado dessas palavras. Ao
ouvir do Abençoado, os bhikkhus o recordarão.”<o:p></o:p></span></p>


<p class=Normal> “Então ouçam e
prestem muita atenção àquilo que eu vou dizer.” – “Sim, venerável senhor,” os
bhikkhus responderam. O Abençoado disse o seguinte: <o:p></o:p></span></p>


<p class=Normal> “Bhikkhus, quando a pessoa comum sem instrução é tocada por uma
sensação dolorosa, ela<span style="mso-spacerun: yes">  </span>fica triste,
angustiada e lamenta, bate no peito, chora e fica perturbada. Dessa maneira,
ela sente duas dores, no corpo e na mente. Como se ela fosse atingida por uma
flecha e, logo em seguida, fosse atingida por outra flecha, de modo que ela
sentiria a sensação de dor de duas flechas. Da mesma forma, a pessoa comum sem
instrução é tocada por uma sensação dolorosa, ela<span style="mso-spacerun:
yes">  </span>fica triste, angustiada e lamenta, bate no peito, chora e fica
perturbada. Dessa maneira ela sente duas dores, no corpo e na mente.<o:p></o:p></span></p>


<p class=Normal> “Ao ser tocada por essa mesma sensação dolorosa, ela sente aversão pela
sensação de dor. Sentindo aversão pela sensação dolorosa, a tendência
subjacente à aversão é aquilo que está por detrás disso. Ao ser tocada pela
sensação dolorosa, ela busca prazer nos prazeres sensuais. Por qual razão?
Porque a pessoa comum sem instrução não sabe como escapar das sensações
dolorosas, exceto através dos prazeres sensuais. [<a href="#N1">1</a>] <a
name=R1></a>Quando ela busca prazer nos prazeres sensuais, a tendência
subjacente ao desejo sensual é aquilo que está por detrás disso. Ela não
compreende como na verdade é a origem e a cessação, a gratificação, o perigo e
a escapatória dessas sensações. Quando ela não compreende essas coisas, a
tendência subjacente à ignorância em relação à sensação nem dolorosa, nem
prazerosa é aquilo que está por detrás disso. <o:p></o:p></span></p>


<p class=Normal> “Se ela sentir uma sensação prazerosa, ela sente isso com apego. Se ela
sentir uma sensação dolorosa, ela sente isso com apego. Se ela sentir uma
sensação nem dolorosa, nem prazerosa, ela sente isso com apego. Essa, bhikkhus,
é uma pessoa comum sem instrução que está apegada ao nascimento,
envelhecimento, morte, tristeza, lamentação, dor, angústia e desespero; ela está
apegada ao sofrimento, eu digo.<o:p></o:p></span></p>


<p class=Normal> “Bhikkhus, quando um nobre discípulo bem instruído é tocado por uma
sensação dolorosa, ele não<span style="mso-spacerun: yes">  </span>fica triste,
angustiado e lamenta, não bate no peito, chora e fica perturbado. Ele sente
apenas uma sensação - no corpo, não na mente. Como se ele fosse
atingido por uma flecha e, não fosse atingido por outra flecha, de modo que ele
sentiria a sensação de dor de uma<span style="mso-spacerun: yes"> 
</span>flecha só. Da mesma forma, um nobre discípulo bem instruído é tocado por
uma sensação dolorosa, ele não<span style="mso-spacerun: yes">  </span>fica
triste, angustiado e lamenta, não bate no peito, chora e fica perturbado. Ele
sente apenas uma sensação - no corpo, não na mente.<o:p></o:p></span></p>


<p class=Normal> “Ao ser tocado por essa mesma sensação dolorosa, ele não sente aversão
pela sensação de dor, a tendência subjacente à aversão não está por detrás
disso. Ao ser tocado pela sensação dolorosa, ele não busca prazer nos prazeres
sensuais. Por qual razão? Porque o nobre discípulo bem instruído sabe como
escapar das sensações dolorosas de outro modo que através dos prazeres
sensuais. Visto que ele não busca prazer nos prazeres sensuais, a tendência
subjacente ao desejo sensual não está por detrás disso. Ele compreende como na
verdade é a origem e a cessação, a gratificação, o perigo e a escapatória
dessas sensações. Visto que ele compreende essas coisas, a tendência subjacente
à ignorância em relação à sensação nem dolorosa, nem prazerosa não está por
detrás disso. <o:p></o:p></span></p>


<p class=Normal> “Se ele sentir uma sensação prazerosa, ele sente isso desapegado. Se
ele sentir uma sensação dolorosa, ele sente isso desapegado. Se ele sentir uma
sensação nem dolorosa, nem prazerosa, ele sente isso desapegado. Esse,
bhikkhus, é um nobre discípulo bem instruído que está desapegado do nascimento,
envelhecimento, morte, tristeza, lamentação, dor, angústia e desespero; ele
está desapegado do sofrimento, eu digo.<o:p></o:p></span></p>


<p class=Normal>  “Essa, bhikkhus, é a variação, a distinção, a diferença entre o
nobre discípulo bem instruído e a pessoa comum sem instrução.”<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>
<span style='mso-tab-count:1'>            </span>O sábio, bem
instruído, não sente <br>

a sensação [mental] prazerosa
e dolorosa.<br>

Essa é a grande
diferença entre<br>

o sábio e o mundano.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>
Para o sábio que
compreendeu o Dhamma,<br>

que com clareza vê
este mundo e o próximo,<br>

coisas desejáveis não
provocam a sua mente,<br>

para com as
indesejáveis ele não sente aversão.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>
Para ele a atração e a
repulsa não mais existem;<br>

ambas foram extintas,
eliminadas.<br>

Tendo experimentado o
estado imaculado, livre do sofrimento,<br>

aquele que transcendeu
a existência compreende corretamente.<o:p></o:p></span></p>



