<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“E como, bhikkhus, um bhikkhu desenvolve e cultiva o Nobre Caminho
Óctuplo para ele declivar, tender e se inclinar na direção de Nibbana? Neste
caso, bhikkhus, um bhikkhu desenvolve o entendimento correto, que tem como base
o afastamento, desapego e cessação, que amadurece no abandono ... Ele
desenvolve a concentração correta, que tem como base o afastamento, desapego e
cessação, que amadurece no abandono. É dessa forma, bhikkhus, que um bhikkhu
desenvolve e cultiva o Nobre Caminho Óctuplo para ele declivar, tender e se
inclinar na direção de Nibbana.”[<a href="#N1">1</a>]<span style='mso-tab-count:
