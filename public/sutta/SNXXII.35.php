<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em Savathi.
Então, um certo bhikkhu foi até o Abençoado e depois de cumprimentá-lo sentou a
um lado e disse: “Seria bom, venerável senhor, se o Abençoado pudesse me
ensinar o Dhamma de forma resumida, de modo que, tendo ouvido o Dhamma do
Abençoado, eu possa permanecer só, isolado, diligente, ardente e decidido.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhu,
qualquer coisa com a qual alguém permaneça obcecado, [<a href="#N1">1</a>] <a
name=R1></a>com base nisso ele será avaliado. Qualquer coisa com a qual alguém
não permaneça obcecado, ele não será avaliado com base nisso.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu
compreendo, Abençoado! Eu compreendo, Iluminado!”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como,
bhikkhu, você compreende o significado em detalhe do que eu disse de forma
resumida?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se alguém
permanecer obcecado com a forma, senhor, é com base nisso que ele será avaliado.
<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se alguém
permanecer obcecado com a sensação...<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se alguém
permanecer obcecado com a percepção...<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se alguém
permanecer obcecado com as formações volitivas...<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style="mso-spacerun: yes"> </span>“Se alguém permanecer obcecado com a
consciência, senhor, é com base nisso que ele será avaliado. [<a href="#N2">2</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Mas, se alguém não permanecer obcecado com a forma, senhor, ele não
será avaliado com base nisso.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se alguém
não permanecer obcecado com a sensação...<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se alguém
não permanecer obcecado com a percepção...<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se alguém
não permanecer obcecado com as formações volitivas...<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se alguém
não permanecer obcecado com a consciência, senhor, ele não será avaliado com base
nisso. [<a href="#N3">3</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Senhor, assim é como compreendo o significado em detalhe do que o
Abençoado disse de forma resumida.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Muito bem,
bhikkhu. É bom que seja assim que você tenha entendido o significado em detalhe
do que eu disse de forma resumida.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se alguém
permanecer obcecado com a forma, bhikkhu, é com base nisso que ele será avaliado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se alguém
permanecer obcecado com a sensação...<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se alguém
permanecer obcecado com a percepção...<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se alguém
permanecer obcecado com as formações volitivas...<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style="mso-spacerun: yes"> </span>“Se alguém permanecer obcecado com a
consciência, bhikkhu, é com base nisso que ele será avaliado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas se
alguém não permanecer obcecado com a forma, bhikkhu, ele não será avaliado com
base nisso.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se alguém
não permanecer obcecado com a sensação...<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se alguém
não permanecer obcecado com a percepção...<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se alguém
não permanecer obcecado com as formações volitivas...<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se alguém
não permanecer obcecado com a consciência, bhikkhu, ele não será avaliado com
base nisso. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Assim
é<span style="mso-spacerun: yes">  </span>como o significado em detalhe do que
eu disse de forma resumida deve ser entendido.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o
bhikkhu, satisfeito e contente com as palavras do Abençoado, levantou do seu
assento e depois de homenagear o Abençoado, mantendo-o à sua direita, partiu.
Depois, permanecendo só, isolado, diligente, ardente e decidido, depois de não
muito tempo, ele alcançou e permaneceu no objetivo supremo da vida santa pelo
qual membros de um clã deixam a vida em família pela vida santa, tendo
conhecido e realizado por si mesmo no aqui e agora. Ele soube: “O nascimento
foi destruído, a vida santa foi vivida, o que deveria ser feito foi feito, não
há mais vir a ser a nenhum estado.” E assim ele se tornou mais um dos arahants.<o:p></o:p></span></p>


