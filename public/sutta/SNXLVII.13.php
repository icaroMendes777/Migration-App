<p class=Normal> Certa ocasião o
Abençoado estava em Savathi, no Bosque de Jeta, no Parque de Anathapindika.
Agora, naquela ocasião o Venerável Sariputta estava entre os habitantes de
Magadha em Nalakagama – <span class=GramE>doente, atormentado</span> e
gravemente enfermo e o noviço Cunda era o seu acompanhante. Então, devido a
essa enfermidade, o Ven. Sariputta realizou o parinibbana. <o:p></o:p></span></p>


<p class=Normal> O noviço Cunda,
tomando a tigela e os mantos do Ven. Sariputta foi até Savathi, no Bosque de
Jeta, no Parque de Anathapindika. Lá ele foi até o Venerável Ananda e depois de
cumprimentá-lo sentou a um lado e disse: &quot;Venerável senhor, o Venerável
Sariputta realizou o parinibbana. Aqui estão a sua tigela e mantos.<span
class=GramE>&quot;</span> <o:p></o:p></span></p>


<p class=Normal> &quot;Cunda, meu amigo,
essa notícia é motivo para ir ver o Abençoado. Venha, vamos até o Abençoado
para relatar-lhe esse assunto.&quot; <o:p></o:p></span></p>


<p class=Normal> &quot;Sim
venerável senhor&quot; o noviço Cunda respondeu. <o:p></o:p></span></p>


<p class=Normal> Então o Ven.
Ananda e o noviço Cunda foram até o Abençoado e depois de cumprimentá-lo sentaram<span
class=GramE><span style='mso-spacerun:yes'>  </span></span>a um lado e o
Venerável Ananda disse: &quot;Este noviço Cunda, venerável senhor, disse que o
Venerável Sariputta realizou o parinibbana. Aqui estão a sua tigela e mantos.
Venerável senhor, desde que ouvi que o Venerável Sariputta realizou o parinibbana,
é como se o meu corpo estivesse sedado, eu me senti perdido, as coisas não
estavam claras para mim.&quot; <o:p></o:p></span></p>


<p class=Normal> &quot;Porque,
Ananda, quando Sariputta realizou<span class=GramE><span
style='mso-spacerun:yes'>  </span></span>o parinibbana ele levou com ele o
seu agregado da virtude, ou o seu agregado da concentração, ou o seu agregado da sabedoria, ou o seu agregado da
libertação, ou o seu agregado do conhecimento e visão da libertação?&quot; <o:p></o:p></span></p>


<p class=Normal> &quot;Não, ele
não levou, venerável senhor. Mas para mim o Venerável Sariputta era um
conselheiro e instrutor, ele me instruía, motivava, estimulava e encorajava.
Ele era incansável no ensinamento do Dhamma; ele era de grande ajuda para os
seus companheiros na vida santa. Nós nos recordamos do alimento do Dhamma, da
riqueza do Dhamma, da ajuda no Dhamma dada pelo Venerável Sariputta.”<o:p></o:p></span></p>


<p class=Normal> <span lang=PT-BR>&quot;Ananda, eu já não lhe disse antes: todas
aquelas coisas que para nós são queridas, estimadas e prazerosas estão sujeitas
à mudança, separação e alteração? Então, como poderia ser possível que tudo
aquilo que nasce, veio a ser, condicionado, que está sujeito à dissolução, não
desapareça – isso é impossível. É como se o maior galho de uma grande árvore
possuidora de cerne caísse: da mesma forma, Ananda, na grande Sangha dos
bhikkhus possuidora de cerne, Sariputta realizou o parinibbana. Como, Ananda,
poderia ser possível: ‘Que aquilo que nasceu, veio a ser, condicionado, que
está sujeito à dissolução, não desapareça? Isso é <span class=GramE>impossível.</span>’<span
style='mso-spacerun:yes'>  </span></span></p>


<p class=Normal> &quot;Portanto,
Ananda, sejam <span class=GramE>ilhas para vocês mesmos</span>, refúgios para
vocês mesmos, buscando nenhum refúgio externo; com o Dhamma como a sua ilha, o
Dhamma como o seu refúgio, buscando nenhum outro refúgio. E como, Ananda, um
bhikkhu é uma ilha para ele mesmo, um refúgio para ele mesmo, buscando nenhum
refúgio externo; com o Dhamma como a sua ilha, o Dhamma como o seu refúgio,
buscando nenhum outro refúgio? Neste caso, Ananda, um bhikkhu permanece
contemplando o corpo como um corpo, ardente, plenamente consciente e com
atenção plena, tendo colocado de lado a cobiça e o desprazer pelo mundo. Ele
permanece contemplando as sensações como sensações ... mente como mente ...
objetos mentais como objetos mentais, ardente, plenamente consciente e com
atenção plena, tendo colocado de lado a cobiça e o desprazer pelo mundo.<o:p></o:p></span></p>


<p class=Normal> “Esses bhikkhus,
Ananda, que agora ou depois que eu me for, permanecerem como uma ilha para eles
mesmos, <span class=GramE>como um refúgio para eles mesmos, sem nada mais como
refúgio, com o Dhamma como uma ilha, o Dhamma como refúgio, sem nada mais como</span>
refúgio, eles serão para mim os bhikkhus mais eminentes dentre aqueles que têm
interesse em aprender.&quot; <o:p></o:p></span></p>

