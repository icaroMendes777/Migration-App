<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. Então, ao amanhecer, a <a name=PVW>bhikkhuni</a> Sela se vestiu e
tomando a tigela e o manto externo foi para Savatthi esmolar alimentos. Depois
de haver esmolado alimentos em Savatthi e de haver retornado, após a refeição,
ela se dirigiu ao bosque dos Homens Cegos em busca de isolamento. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, Mara, o Senhor do
Mal, desejando despertar medo, trepidação e terror na bhikkhuni Sela, desejando
que ela abandonasse a sua concentração, se aproximou e se dirigiu a ela em
versos: <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR style='mso-ansi-language:PT-BR'>“Por
quem foi criado esse fantoche?<br>
Onde está o criador desse fantoche?<br>
Onde surgiu esse fantoche?<br>
Onde cessa esse fantoche?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Foi então que a bhikkhuni
Sela pensou: “Agora, quem será que recitou esses versos – um ser humano ou um
ser não humano?” Então lhe ocorreu: “Foi Mara, o Senhor do Mal, quem recitou
esses versos desejando despertar medo, trepidação e terror em mim, desejando
que eu abandone a minha concentração.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, a bhikkhuni Sela,
tendo compreendido, “Este é Mara, o Senhor do Mal,” respondeu em versos: <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR style='mso-ansi-language:PT-BR'>“Esse
fantoche não é feito por si mesmo,<br>
nem é essa miséria criada por outrem.<br>
Ele surgiu na dependência de condições,<br>
com a dissolução das condições, ele cessará. <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR style='mso-ansi-language:PT-BR'>Como quando uma semente
é plantada no solo<br>
ela cresce na dependência de alguns fatores:<br>
ela requer ambos os nutrientes do solo<br>
e um fornecimento contínuo de umidade. <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR style='mso-ansi-language:PT-BR'>Da mesma forma, os
agregados e os elementos,<br>
e as seis bases do contato sensual,<br>
surgem na dependência de condições;<br>
com a dissolução das condições, eles cessam.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, Mara, o Senhor do
Mal, compreendendo que, “A bhikkhuni Sela sabe quem sou,” triste e desapontado,
desapareceu no mesmo instante. <o:p></o:p></span></p>



