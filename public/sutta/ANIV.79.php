<p class=Normal> Então o venerável Sariputta foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse: "Venerável senhor, (1) porque para um certa pessoa o seu negócio fracassa? (2) Por que para outra o mesmo tipo de negócio não preenche as suas expectativas? (3) Por que para uma outra o mesmo tipo de negócio preenche as suas expectativas? (4) E por que para uma outra o mesmo tipo de negócio excede as suas expectativas?" </p>

<p class=Normal> (1) "Aqui, Sariputta, alguém vai até um contemplativo ou brâmane e o convida que peça aquilo que necessita, mas ele não dá aquilo que foi pedido. Ao falecer, se retornar a este mundo, qualquer negócio que ele empreenda acabará em fracasso. </p>

<p class=Normal> (2) "Outra pessoa vai até um contemplativo ou brâmane e o convida que peça aquilo que necessita. Ele dá aquilo que foi pedido mas não preenche as expectativas. Ao falecer, se retornar a este mundo, qualquer negócio que ele empreenda não preencherá as suas expectativas. </p>

<p class=Normal> (3) "Outra pessoa vai até um contemplativo ou brâmane e o convida que peça aquilo que necessita. Ele dá aquilo que foi pedido e preenche as expectativas. Ao falecer, se retornar a este mundo, qualquer negócio que ele empreenda preencherá as suas expectativas. </p>

<p class=Normal> (4) "Outra pessoa vai até um contemplativo ou brâmane e o convida que peça aquilo que necessita. Ele dá aquilo que foi pedido e excede as expectativas. Ao falecer, se retornar a este mundo, qualquer negócio que ele empreenda excederá as suas expectativas. </p>

<p class=Normal> "Essa, Sariputta, é a razão porque para um certa pessoa o seu negócio fracassa, para outra o mesmo tipo de negócio não preenche as suas expectativas, para uma outra o mesmo tipo de negócio preenche as suas expectativas, e para uma outra o mesmo tipo de negócio excede as suas expectativas." </p>

