<p class=Normal>Em Savatthi. “Bhikkhus, um nobre discípulo bem instruído não pensa: ‘Quando existe o quê, o quê vem a ser? Com o surgimento do quê, o quê surge? Quando existe o quê, as formações volitivas vêm a ser? Quando existe o quê, a consciência vem a ser?  Quando existe o quê, a mentalidade-materialidade (nome e forma) vem a ser? ... Quando existe o quê, o envelhecimento e morte vêm a ser?’” </p>

<p class=Normal> “Ao invés disso, bhikkhus, o nobre discípulo bem instruído tem conhecimento disso que é independente dos outros: ‘Quando existe isso, aquilo existe; com o surgimento disso, aquilo surge. Quando há ignorância, as formações volitivas surgem. Quando há formações volitivas, a consciência surge. Quando há consciência, a mentalidade-materialidade (nome e forma) surge. Quando há nascimento, o envelhecimento e morte surgem.’ Ele compreende assim : ‘Desse modo o mundo tem origem.’” </p>  

<p class=Normal> “Bhikkhus, um nobre discípulo bem instruído não pensa:  ‘Quando não existe o quê, o quê não vem a ser? Com a cessação do quê, o quê cessa? Quando não existe o quê, as formações volitivas não vêm a ser? Quando não existe o quê, a consciência não vem a ser?  Quando não existe o quê, a mentalidade-materialidade (nome e forma) não vem a ser? ... Quando não existe o quê, o envelhecimento e morte não vêm a ser?’” </p>

<p class=Normal> “Ao invés disso, bhikkhus, o nobre discípulo bem instruído tem conhecimento disso que é independente dos outros: ‘Quando não existe isso, aquilo não existe; com a cessação disso, aquilo cessa. Quando não há ignorância, as formações volitivas não surgem. Quando não há formações volitivas, a consciência não surge. Quando não há consciência, a mentalidade-materialidade (nome e forma) não surge. Quando não há nascimento, o envelhecimento e morte não surgem.’ Ele compreende assim : ‘Desse modo o mundo cessa.’” </p>

<p class=Normal> “Bhikkhus, quando um nobre discípulo assim compreende como na verdade é a origem e a cessação do mundo, ele é chamado um nobre discípulo com o entendimento perfeito, com a visão perfeita; ele realizou o verdadeiro Dhamma, ele vê esse verdadeiro Dhamma, ele possui o conhecimento de um treinando, que entrou na correnteza do Dhamma, um nobre com a sabedoria penetrante que está às portas do Imortal.” </p>

