<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] O entendimento de que a alma (<i>jiva) </i>e o corpo são o mesmo é
materialismo, que reduz a alma ao corpo. A idéia seguinte de que a alma e o
corpo são diferentes é a idéia da eternidade, que considera a alma um princípio
espiritual que persiste e que pode ter existência independente do corpo. [<a
href="#R1">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[2] A idéia
de que um Tathagata existe após a morte é uma forma de crença na eternidade que
considera o Tathagata, ou o indivíduo espiritualmente perfeito, como o
possuidor de um eu que alcança a libertação eterna após a morte do corpo. A
idéia de que o Tathagata não existe após a morte também identifica o Tathagata
com um eu, mas defende que esse eu é aniquilado com a morte do corpo. A
terceira idéia é uma tentativa de sintetizar essas duas, que o Buda rejeita
porque ambos os componentes envolvem o entendimento incorreto. A quarta idéia
parece ser uma tentativa cética de rejeitar ambas as alternativas ou de evitar
assumir uma posição definitiva. [<a href="#R2">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N3></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[3] Em Pali
existe um jogo de palavras entre <i>ditthigata, </i>“idéia especulativa”, que o
Tathagata colocou de lado, e <i>dittha, </i>aquilo que foi “visto” pelo
Tathagata através do conhecimento direto, isto é, a origem e cessação dos cinco
agregados. [<a href="#R3">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N4></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[4] MA diz
que “não renasce” na verdade se aplica, no sentido de que um arahant não irá
estar sujeito a uma nova existência. Mas se Vacchagotta ouvisse isso ele iria
entendê-lo de forma errada como aniquilação e dessa forma o Buda nega que se
aplica no sentido de que a aniquilação não é uma posição defensável.<span
style='mso-spacerun:yes'>  </span>[<a href="#R4">Retorna</a>]<o:p></o:p></span></p>

<p class=Normal style='margin-top:12.0pt'><a name=N5></a><span lang=PT-BR
