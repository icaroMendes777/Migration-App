<p class=Normal>Então um certo bhikkhu foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse: </p> 

<p class=Normal> “Venerável senhor, assim dizem, ‘com as faculdades consumadas, com as faculdades consumadas.’ De que modo, venerável senhor, alguém tem as faculdades consumadas?” </p>

<p class=Normal> “Bhikkhu, se enquanto alguém permanece contemplando a origem e a cessação na faculdade do olho, ele experimenta o desencantamento com relação à faculdade do olho; se enquanto alguém permanece  contemplando a origem e a cessação na faculdade do ouvido,  ele experimenta o desencantamento com relação à faculdade do ouvido; ... se, enquanto alguém permanece contemplando a origem e a cessação na faculdade da mente, ele experimenta o desencantamento com relação à faculdade da mente, então experimentando o desencantamento, ele se torna desapegado. Quando [a mente] está libertada, surge o conhecimento: ‘Libertada.’ Ele compreende que: ‘O nascimento foi destruído, a vida santa foi vivida, o que deveria ser feito foi feito, não há mais vir a ser a nenhum estado.’ É desse modo, bhikkhu, que alguém tem as faculdades consumadas.” </p>

