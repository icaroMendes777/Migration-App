<p class=Normal> "Bhikkhus, há essas cinco coisas que conduzem ao declínio e desaparecimento do verdadeiro Dhamma. Quais cinco? (1) Aqui, os bhikkhus não ouvem respeitosamente o Dhamma; (2) eles não aprendem respeitosamente o Dhamma; (3) eles não retêm respeitosamente o Dhamma na mente; (4) eles não examinam respeitosamente os ensinamentos que reteram na mente; (5) eles não compreendem respeitosamente o significado do Dhamma e depois não praticam de acordo com o Dhamma. Essas cinco coisas conduzem ao declínio e desaparecimento do verdadeiro Dhamma.</p>

<p class=Normal> "Bhikkhus, há essas outras cinco coisas que conduzem à continuidade, ao não declínio e ao não desaparecimento do verdadeiro Dhamma. Quais cinco? (1) Aqui, os bhikkhus ouvem respeitosamente o Dhamma; (2) eles aprendem respeitosamente o Dhamma; (3) eles retêm respeitosamente o Dhamma na mente; (4) eles examinam respeitosamente os ensinamentos que reteram na mente; (5) eles compreendem respeitosamente o significado do Dhamma e depois praticam de acordo com o Dhamma. Essas cinco coisas conduzem à continuidade, ao não declínio e ao não desaparecimento do verdadeiro Dhamma."</p>

