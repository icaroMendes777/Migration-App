<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. </span>Assim
ouvi. Em certa ocasião o Abençoado estava em<span lang=PT-BR style='mso-ansi-language:
PT-BR'> Catuma num bosque de prunos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. Agora,
naquela ocasião quinhentos bhikkhus liderados pelo venerável Sariputta e pelo
venerável Maha Moggallana vieram até Catuma para ver o Abençoado. Enquanto os
bhikkhus visitantes estavam cumprimentando os bhikkhus residentes, estavam
preparando os lugares para descanso, guardando as suas tigelas e mantos
externos, eles faziam muito ruído e barulho.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. Então o
Abençoado se dirigiu ao venerável Ananda assim: “Ananda, quem são essas pessoas
ruidosas e barulhentas? Alguém poderia pensar que eles são pescadores
apregoando peixes.” [<a href="#N1">1</a>]<o:p></o:p></span></p>


<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, eles são os quinhentos bhikkhus liderados pelo venerável Sariputta e
pelo venerável Maha Moggallana que vieram até Catuma para ver o Abençoado. Enquanto
os bhikkhus visitantes cumprimentam os bhikkhus residentes, preparam os
lugares para descanso, guardam as suas tigelas e mantos externos, eles fazem
muito ruído e barulho.”<span style="mso-spacerun: yes">  </span><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. “Então,
Ananda, diga a esses bhikkhus em meu nome que o Mestre chama os veneráveis.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
venerável senhor,” ele respondeu e foi até aqueles bhikkhus e lhes disse: “O
Mestre chama os veneráveis.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
amigo,” eles responderam e foram até o Abençoado e depois de cumprimentá-lo sentaram
a um lado. Estando sentados o Abençoado lhes perguntou: “Bhikkhus porque vocês
são tão ruidosos e barulhentos? Alguém poderia pensar que vocês são pescadores
apregoando peixes.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, nós somos os quinhentos bhikkhus liderados pelo venerável Sariputta e
pelo venerável Maha Moggallana que viemos até Catuma para ver o Abençoado. E
foi enquanto nós, bhikkhus visitantes, cumprimentávamos os bhikkhus residentes, preparávamos os lugares para descanso,
 guardávamos as nossas tigelas e mantos
externos, que fizemos muito ruído e barulho.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. “Vão,
bhikkhus, eu os dispenso. Vocês não podem estar comigo.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
venerável senhor,” eles responderam e se levantaram dos seus assentos e depois
de homenagear o Abençoado, mantendo-o à sua direita, guardaram as coisas nos
seus devidos lugares e tomando a tigela e o manto externo, partiram. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. Agora,
naquela ocasião os Sakyas de Catuma haviam se reunido no seu salão de
assembléias para tratar de negócios. Vendo os bhikkhus vindo à distância, eles
foram até eles e perguntaram: “Onde vocês estão indo, veneráveis senhores?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigos, a
Sangha dos bhikkhus foi dispensada pelo Abençoado.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então, que
os veneráveis sentem um pouco. Talvez nós possamos restaurar a confiança dele.”
“Sim, amigos, eles responderam.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. Então,
os Sakyas de Catuma foram até o Abençoado e depois de cumprimentá-lo sentaram a
um lado e disseram:<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, que o Abençoado se deleite com a Sangha dos bhikkhus; venerável senhor,
que o Abençoado dê as boas vindas à Sangha dos bhikkhus; venerável senhor, que
o Abençoado demonstre compaixão para com Sangha dos bhikkhus agora, tal como
ele costumava demonstrar compaixão para com eles no passado. Venerável senhor,
aqui há bhikkhus novos, que acabam de entrar para a vida santa, que recém-chegaram
neste Dhamma e Disciplina. Se eles não tiverem a oportunidade de ver o
Abençoado, poderá ocorrer neles alguma mudança ou alteração. [<a href="#NA">A</a>]<a name=RA></a> Venerável senhor,
tal como quando mudas tenras não obtêm água, poderá nelas ocorrer alguma
mudança ou alteração, assim também, venerável senhor, há bhikkhus novos, que
acabam de entrar para a vida santa, que recém- chegaram neste Dhamma e
Disciplina. Se eles não tiverem a oportunidade de ver o Abençoado, poderá
ocorrer neles alguma mudança ou alteração. Venerável senhor, tal como quando um
bezerro tenro não vê a mãe,<span style="mso-spacerun: yes">  </span>poderá nele
ocorrer alguma mudança ou alteração, assim também, venerável senhor, há
bhikkhus novos, que acabam de entrar para a vida santa, que recém-chegaram neste
Dhamma e Disciplina. Se eles não tiverem a oportunidade de ver o Abençoado,
poderá ocorrer neles alguma mudança ou alteração. Venerável senhor, que o
Abençoado se deleite com a Sangha dos bhikkhus; venerável senhor, que o
Abençoado dê as boas vindas à Sangha dos bhikkhus; venerável senhor, que o
Abençoado demonstre compaixão para com Sangha dos bhikkhus agora, como ele
costumava demonstrar compaixão para com eles no passado.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. Então o
Brahma Sahampati [<a href="#N2">2</a>]<a name=R2></a> soube com a sua mente o
pensamento na mente do Abençoado, então, com a mesma rapidez com que um homem
forte pode estender o seu braço flexionado ou flexionar o seu braço estendido,
Brahma Sahampati desapareceu do mundo de Brahma e apareceu na frente do
Abençoado. Ele arrumou o seu manto externo sobre o ombro e juntou as mãos
numa reverenciosa saudação, dizendo:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9.
“Venerável senhor, que o Abençoado se deleite com a Sangha dos bhikkhus;
venerável senhor, que o Abençoado dê as boas vindas à Sangha dos bhikkhus ...
(igual ao verso 7) ... agora, como ele costumava demonstrar compaixão para com
eles no passado.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. Os
Sakyas de Catuma e o Brahma Sahampati foram capazes de restaurar a confiança do
Abençoado com os símiles das mudas e do bezerro tenro.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. Então,
o venerável Maha Moggallana se dirigiu aos bhikkhus da seguinte forma:
“Levantem-se, amigos, tomem as suas tigelas e os mantos externos. A confiança
foi restaurada no Abençoado pelos Sakyas de Catuma e pelo Brahma Sahampati com
os símiles das mudas e do bezerro tenro.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. “Sim,
amigo,” eles responderam e tomando as tigelas e os mantos externos eles foram
até o Abençoado e depois de cumprimentá-lo sentaram a um lado. Depois de terem
feito isso o Abençoado perguntou ao venerável Sariputta: “O que você pensou,
Sariputta, quando a Sangha dos bhikkhus foi por mim dispensada?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, eu pensei o seguinte: ‘A Sangha dos bhikkhus foi dispensada pelo
Abençoado. O Abençoado irá agora permanecer inativo, dedicado a uma estada
prazerosa<span style="mso-spacerun: yes">  </span>aqui e agora, e nós também
deveríamos permanecer inativos, dedicados a uma estada prazerosa aqui e agora.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Pare,
Sariputta, pare! Você não deve mais entreter esse tipo de pensamento.” [<a
href="#N3">3</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>13. Então, o Abençoado se dirigiu ao venerável Maha Moggallana: “O que
você pensou, Moggallana, quando a Sangha dos bhikkhus foi por mim dispensada?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, eu pensei o seguinte: ‘A Sangha dos bhikkhus foi dispensada pelo
Abençoado. O Abençoado irá agora permanecer inativo, dedicado a uma estada
prazerosa aqui e agora. Agora, o venerável Sariputta e eu iremos liderar a
Sangha dos bhikkhus.’”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Muito bem,
Moggallana! Ou eu devo liderar a Sangha dos bhikkhus ou então Sariputta e
Moggallana devem liderá-la. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14. Então,
o Abençoado </span>dirigiu-se aos <span lang=PT-BR style='mso-ansi-language:
PT-BR'>bhikkhus</span> da seguinte forma<span lang=PT-BR style='mso-ansi-language:
PT-BR'>:<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus, há
esses quatro tipos de medo que podem ser esperados por aqueles que entram na
água.[<a href="#N4">4</a>] <a name=R4></a>Quais são os quatro? Eles são: medo das
ondas, medo dos crocodilos, medo dos redemoinhos e o medo dos tubarões. Esses
são os quatro tipos de medo que podem ser esperados por aqueles que entram na
água.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. “Da
mesma forma, bhikkhus, há quatro tipos de medo que podem ser esperados por
certas pessoas que deixaram a vida em família e seguiram a vida santa neste
Dhamma e Disciplina. Quais são os quatro? Eles são: medo das ondas, medo dos
crocodilos, medo dos redemoinhos e o medo dos tubarões. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16. “O que,
bhikkhus, é o medo das ondas?<span style="mso-spacerun: yes">  </span>Aqui, um
membro de um clã com base na fé deixa a vida em família e segue a vida santa,
considerando: ‘Eu sou uma vítima do nascimento, envelhecimento e morte, da
tristeza, lamentação, dor, angústia e desespero; eu sou uma vítima do
sofrimento, uma presa do sofrimento. Com certeza, um fim a toda essa massa de
sofrimento pode ser apreendido.’ Então, depois dele ter assim seguido a vida
santa, os seus companheiros na vida santa o aconselham e instruem assim: ‘Você
deve se mover de cá para lá assim; você deve olhar para frente e para o lado
assim; você deve flexionar e estender os membros assim; você deve usar o manto
de retalhos, a tigela e o manto externo assim.’ Então, ele pensa: ‘Antes,
quando vivíamos em família, nós aconselhávamos e instruíamos os outros, e agora
esses [bhikkhus], que, parece, poderiam ser nossos filhos ou netos, pensam que
podem nos aconselhar e instruir.’ E assim ele abandona o treinamento e<span
style="mso-spacerun: yes">  </span>reverte à vida inferior. Ele é chamado
aquele que abandonou o treinamento e regressou para a vida comum porque ele
ficou assustado com o medo das ondas. Agora ‘ondas’ é um termo para a
desesperança enraivecida. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17. “O que,
bhikkhus, é o medo dos crocodilos?<span style="mso-spacerun: yes"> 
</span>Aqui, um membro de um clã com base na fé deixa a vida em família e segue
a vida santa, considerando: ‘Eu sou uma vítima do nascimento, envelhecimento e
morte, da tristeza, lamentação, dor, angústia e desespero; eu sou uma vítima do
sofrimento, uma presa do sofrimento. Com certeza, um fim a toda essa massa de
sofrimento pode ser apreendido.’ Então, depois dele ter assim seguido a vida
santa, os seus companheiros na vida santa o aconselham e instruem assim: ‘Isto
pode ser consumido por você, isso não pode ser consumido por você; isso pode
ser comido por você, isso não pode ser comido por você; isso pode ser provado
por você, isso não pode ser provado por você; isso pode ser bebido por você, isso
não pode ser bebido por você. [<a href="#n5">5</a>] <a name=R5></a>Você pode
consumir aquilo que é permitido, você não pode consumir aquilo que não é
permitido; você pode comer aquilo que é permitido, você não pode comer aquilo
que não é permitido; você pode provar aquilo que é permitido, você não pode
provar aquilo que não é permitido; você pode beber aquilo que é permitido, você
não pode beber aquilo que não é permitido. Você pode consumir comida dentro do
horário apropriado, você não pode consumir comida fora do horário apropriado;
você pode comer dentro do horário apropriado, você não pode comer fora do
horário apropriado; você pode provar comida dentro do horário apropriado, você
não pode provar comida fora do horário apropriado; você pode beber dentro do horário
apropriado, você não pode beber fora do horário apropriado. [<a href="#n6">6</a>]<o:p></o:p></span></p>


<p class=Normal><a name=R6></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então, ele
pensa: ‘Antes, quando vivíamos em família, nós consumíamos aquilo que
gostávamos e não consumíamos aquilo que não gostávamos; nós comíamos aquilo que
gostávamos e não comíamos aquilo que não gostávamos; nós provávamos aquilo que
gostávamos e não provávamos aquilo que não gostávamos; nós bebíamos aquilo que
gostávamos e não bebíamos aquilo que não gostávamos. Nós consumíamos aquilo que
era permitido e aquilo que não era permitido; nós comíamos aquilo que era
permitido e aquilo que não era permitido; nós provávamos aquilo que era
permitido e aquilo que não era permitido; nós bebíamos aquilo que era permitido
e aquilo que não era permitido. Nós consumíamos comida dentro do horário
apropriado e fora do horário apropriado; nós comíamos dentro do horário
apropriado e fora do horário apropriado; nós provávamos comida dentro do
horário apropriado e fora do horário apropriado; nós bebíamos dentro do horário
apropriado e fora do horário apropriado. Agora, quando chefes de família
devotos nos dão bons alimentos de vários tipos durante o dia, fora do horário
apropriado, é como se esses [bhikkhus] colocassem uma mordaça nas nossas
bocas.’ E assim ele abandona o treinamento e<span style="mso-spacerun: yes"> 
</span>reverte à vida inferior. Ele é chamado aquele que abandonou o
treinamento e regressou para a vida comum porque ele ficou assustado com o medo
dos crocodilos. Agora ‘crocodilos’ é um termo para a<span style="mso-spacerun:
yes">  </span>gula. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18. “O que,
bhikkhus, é o medo dos redemoinhos?<span style="mso-spacerun: yes"> 
</span>Aqui, um membro de um clã com base na fé, deixa a vida em família e
segue a vida santa, considerando: ‘Eu sou uma vítima do nascimento,
envelhecimento e morte, da tristeza, lamentação, dor, angústia e desespero; eu
sou uma vítima do sofrimento, uma presa do sofrimento. Com certeza, um fim a
toda essa massa de sofrimento pode ser apreendido.’ Então, depois dele ter
assim seguido a vida santa, ao amanhecer ele se veste, e tomando a tigela e o
manto externo ele vai para um vilarejo ou cidade esmolar alimentos, com o corpo
descuidado, com a linguagem descuidada, com a atenção plena não estabelecida e
com as faculdades dos sentidos descontroladas. Ele vê algum chefe de família ou
o filho de um chefe de família provido e dotado com os cinco elementos do prazer
sensual desfrutando deles com prazer. Ele pensa o seguinte: ‘Antes, quando
vivíamos a vida em família, éramos providos e dotados com os cinco elementos do
prazer sensual e nós desfrutávamos deles com prazer. Minha família tem riqueza;
eu posso tanto desfrutar da riqueza como realizar méritos.’ E assim ele
abandona o treinamento e<span style="mso-spacerun: yes">  </span>reverte à vida
inferior. Ele é chamado aquele que abandonou o treinamento e regressou para a vida comum porque ele ficou assustado com o medo dos redemoinhos. Agora
‘redemoinhos’ é um termo para os cinco elementos do prazer sensual.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>19. “O que,
bhikkhus, é o medo dos tubarões?<span style="mso-spacerun: yes">  </span>Aqui,
um membro de um clã com base na fé deixa a vida em família e segue a vida
santa, considerando: ‘Eu sou uma vítima do nascimento, envelhecimento e morte,
da tristeza, lamentação, dor, angústia e desespero; eu sou uma vítima do
sofrimento, uma presa do sofrimento. Com certeza, um fim a toda essa massa de
sofrimento pode ser apreendido.’ Então, depois dele ter assim seguido a vida
santa, ao amanhecer ele se veste, e tomando a tigela e o manto externo ele vai
para um vilarejo ou cidade esmolar alimentos, com o corpo descuidado, com a
linguagem descuidada, com a atenção plena não estabelecida e com as faculdades
dos sentidos descontroladas. Ele vê uma mulher vestida com pouca roupa. Ao ver
tal mulher, a paixão infecta a sua mente. Como a sua mente está infectada pela
paixão, ele abandona o treinamento e<span style="mso-spacerun: yes"> 
</span>reverte à vida inferior. Ele é chamado aquele que abandonou o
treinamento e regressou para a vida comum porque ele ficou assustado com o medo
dos tubarões. Agora ‘tubarões’ é um termo para as mulheres.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>20.
“Bhikkhus, esses são os quatro tipos de medo que podem ser esperados por
certas pessoas que deixaram a vida em família e seguiram a vida santa neste
Dhamma e Disciplina<o:p></o:p></span></p>



<p class=Normal>Isso foi o que disse o Abençoado. Os bhikkhus ficaram
satisfeitos e contentes com as palavras do Abençoado.<span lang=PT-BR
style='mso-ansi-language:PT-BR'><o:p></o:p></span></p>


