<p class=Normal> Certa ocasião o venerável Ananda e o venerável Udayi estavam em Kosambi no Parque de Ghosita. Então ao anoitecer o venerável Udayi saiu do seu isolamento e foi até o venerável Ananda e ambos se cumprimentaram. Quando a conversa amigável e cortês havia terminado, ele sentou a um lado e disse: </p>

<p class=Normal> "Amigo Ananda, de várias forma a natureza deste corpo tem sido declarada, mostrada, e revelada pelo Abençoado assim: 'Por essa razão o corpo é não-eu.' É possível explicar a natureza dessa consciência de um modo semelhante - ensinar, proclamar, estabelecer, revelar, analisar, e elucidar assim: 'Por essa razão esta consciência é não-eu'?" </p>

<p class=Normal> "É possível, amigo Udayi, a consciência no olho não surge na dependência do olho e das formas?" </p>

<p class=Normal> "Sim, amigo." </p>

<p class=Normal> "Se a causa e condição para o surgimento da consciência no olho cessasse total e completamente sem deixar nenhum resíduo, a consciência no olho poderia ser discernida?" </p>

<p class=Normal> "Não, amigo." </p>

<p class=Normal> "Desse modo, amigo, isso foi declarado, mostrado, e revelado pelo Abençoado assim: 'Por essa razão essa consciência é não-eu.'</p>

<p class=Normal> "A consciência no ouvido não surge na dependência do ouvido e dos sons? ... a consciência na mente não surge na dependência da mente e dos objetos mentais?" </p>

<p class=Normal> "Sim, amigo." </p>

<p class=Normal> "Se a causa e condição para o surgimento da consciência na mente cessasse total e completamente sem deixar nenhum resíduo, a consciência na mente poderia ser discernida?" </p>

<p class=Normal> "Não, amigo." </p>

<p class=Normal> "Desse modo, amigo, isso foi declarado, mostrado, e revelado pelo Abençoado assim: 'Por essa razão essa consciência é não-eu.'</p>

<p class=Normal> "Suponha, amigo, que um homem precisasse de madeira, procurasse madeira, perambulasse em busca de madeira, pegasse um machado afiado e entrasse na floresta. Lá ele veria uma grande bananeira, ereta, jovem, sem frutos. Então ele a cortasse pela raiz, removesse a coroa e desenrolasse as folhas que envolvem o tronco; mas à medida que ele desenrolasse as folhas do tronco, ele não encontraria nem alburno e muito menos madeira. </p>

<p class=Normal> "Do mesmo modo, um bhikkhu não reconhece um eu ou algo que pertença a um eu nessas seis bases para o contato. Visto que ele não reconhece nada assim, ele não se apega a nada no mundo. Não se apegando, ele não fica agitado. Sem estar agitado, ele realiza Nibbana. Ele compreende que: ‘O nascimento foi destruído, a vida santa foi vivida, o que deveria ser feito foi feito, não há mais vir a ser a nenhum estado.’” </p>

