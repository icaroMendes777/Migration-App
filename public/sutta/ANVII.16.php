<p class=Normal> "Bhikkhus, há esses sete tipos de pessoas merecedoras de dádivas, merecedoras de hospitalidade, merecedoras de oferendas, merecedoras de saudações com reverência, um campo inigualável de mérito para o mundo. Quais sete? </p>

<p class=Normal><a name=R1></a> (1) "Aqui, bhikkhus, uma certa pessoa permanece contemplando a impermanência em todos os fenômenos condicionados, percebendo a impermanência, sentindo (experimentando) a impermanência [<a href="#N1">1</a>] constantemente, continuamente focando nisso com a mente sem interrupção, penetrando com a sabedoria. Realizando por si mesmo através do conhecimento direto, aqui e agora, ela entra e permanece na libertação da mente e libertação através da sabedoria que são imaculadas com a destruição de todas as impurezas. Esse é o primeiro tipo de pessoa merecedora de dádivas, merecedora de hospitalidade, merecedora de oferendas, merecedora de saudações com reverência, um campo inigualável de mérito para o mundo. </p>

<p class=Normal> (2) "Outra vez, uma certa pessoa permanece contemplando a impermanência em todos os fenômenos condicionados, percebendo a impermanência, sentindo (experimentando) a impermanência constantemente, continuamente focando nisso com a mente sem interrupção, penetrando com a sabedoria. Para ela a destruição das impurezas e o fim da vida ocorrem simultaneamente. Esse é o segundo tipo de pessoa merecedora de dádivas, merecedora de hospitalidade, merecedora de oferendas, merecedora de saudações com reverência, um campo inigualável de mérito para o mundo. </p>

<p class=Normal><a name=R2></a> (3) "Outra vez, uma certa pessoa permanece contemplando a impermanência em todos os fenômenos condicionados, percebendo a impermanência, sentindo (experimentando) a impermanência constantemente, continuamente focando nisso com a mente sem interrupção, penetrando com a sabedoria. Com a completa destruição dos cinco primeiros grilhões ela realiza nibbana no intervalo. [<a href="#N2">2</a>] Esse é o terceiro tipo de pessoa merecedora de dádivas, merecedora de hospitalidade, merecedora de oferendas, merecedora de saudações com reverência, um campo inigualável de mérito para o mundo. </p>

<p class=Normal><a name=R3></a> (4) " ... Com a completa destruição dos cinco primeiros grilhões ela realiza nibbana ao pousar. [<a href="#N3">3</a>] Esse é o quarto tipo de pessoa ... </p>

<p class=Normal><a name=R4></a> (5) " ... Com a completa destruição dos cinco primeiros grilhões ela realiza nibbana sem esforço. [<a href="#N4">4</a>] Esse é o quinto tipo de pessoa ... </p>

<p class=Normal><a name=R5></a> (6) " ... Com a completa destruição dos cinco primeiros grilhões ela realiza nibbana com esforço. [<a href="#N5">5</a>] Esse é o sexto tipo de pessoa ... </p>

<p class=Normal><a name=R6></a> (7) "Outra vez, uma certa pessoa permanece contemplando a impermanência em todos os fenômenos condicionados, percebendo a impermanência, sentindo (experimentando) a impermanência constantemente, continuamente focando nisso com a mente sem interrupção, penetrando com a sabedoria. Com a completa destruição dos cinco primeiros grilhões ela estará destinada a um plano superior, o plano Akanittha. [<a href="#N6">6</a>] Esse é o sétimo tipo de pessoa merecedora de dádivas, merecedora de hospitalidade, merecedora de oferendas, merecedora de saudações com reverência, um campo inigualável de mérito para o mundo. </p>

<p class=Normal>"Esses, bhikkhus, são os sete tipos de pessoas merecedoras de dádivas, merecedoras de hospitalidade, merecedoras de oferendas, merecedoras de saudações com reverência, um campo inigualável de mérito para o mundo." </p>


