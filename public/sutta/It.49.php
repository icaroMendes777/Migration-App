<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isto foi
dito pelo Abençoado, dito pelo Arahant, assim ouvi: “Suportados por dois tipos
de idéias, alguns seres humanos e divinos aderem, outros seres humanos e
divinos se excedem, enquanto que aqueles com visão vêem. <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como
alguns aderem? Seres humanos e divinos apreciam o ser/existir, se deliciam com
o ser/existir, estão satisfeitos com o ser/existir. Quando o Dhamma é ensinado
com o objetivo de cessar o ser/existir, as suas mentes não se interessam por
aquilo, não são acalmadas por aquilo, não se estabelecem naquilo ou se
determinam por aquilo. Assim é como alguns aderem.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como
alguns se excedem? Alguns, sentindo-se horrorizados, humilhados e enojados com
esse mesmo ser/existir, desfrutam da não existência: ‘Quando este eu, com a
dissolução do corpo, após a morte, morrer e for destruído, e não existir depois
da morte, isso é a paz, isso é excelente, isso é a realidade!’ Assim é como
alguns se excedem. <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como
aqueles com visão vêem? É o caso em que um bhikkhu vê aquilo que veio a ser
como veio a ser. Vendo aquilo que veio a ser como veio a ser, ele pratica para
o desencantamento com relação aquilo que veio a ser, desapego com relação
aquilo que veio a ser, cessação com relação aquilo que veio a ser. Assim é como
aqueles com visão vêem. [<a href="#N1">1</a>]<o:p></o:p></span></p>


