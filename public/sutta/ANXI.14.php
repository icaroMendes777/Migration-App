<p class=Normal> Então o venerável Subhuti junto com o bhikkhu Saddha foram até o Abençoado e depois de cumprimentá-lo sentaram a um lado. O Abençoado disse para o venerável Subhuti: </p>

<p class=Normal> "Qual é o nome desse bhikkhu, Subhuti?" </p>

<p class=Normal> "Seu nome é Saddha, venerável senhor. Ele é o filho de um discípulo leigo dotado de fé, e ele deixou a vida em família pela vida santa com base na fé." </p>

<p class=Normal> "Espero que esse filho de um discípulo leigo dotado de fé, o bhikkhu Saddha, que deixou a vida em família pela vida santa com base na fé, demonstre as manifestações da fé." [<a href="#N1">1</a>]</p>

<p class=Normal><a name=R1></a> “Este é o momento, Abençoado, este é o momento, Iluminado, para que o Abençoado ensine as manifestações da fé. Agora descobrirei se este bhikkhu demonstra, ou não, as manifestações da fé." </p>

<p class=Normal> “Então ouça e preste muita atenção àquilo que eu vou dizer.” – “Sim, venerável senhor,” o venerável Subhuti respondeu. O Abençoado disse o seguinte: </p>

<p class=Normal> (1) "Aqui, Subhuti, um bhikkhu é virtuoso. Ele permanece contido de acordo com as regras do Patimokkha, perfeito na conduta e na sua esfera de atividades. Temendo a menor falha, ele treina adotando os preceitos de virtude. Essa é uma manifestação da fé em alguém dotado de fé. </p>

<p class=Normal> (2) "Outra vez, ele ouviu muito, reteve o que ouviu, memorizou o que ouviu. Todo ensinamento que é admirável no início, admirável no meio, admirável no final, com o correto significado e fraseado e que revela uma vida santa que é completamente perfeita e imaculada: esses ensinamentos ele ouviu com freqüência, memorizou, se recorda, discutiu, investigou com a sua mente e penetrou corretamente com o entendimento. Essa também é uma manifestação da fé em alguém dotado de fé. </p>

<p class=Normal> (3) "Outra vez, ele tem bons amigos, companheiros, e camaradas. Essa também é uma manifestação da fé em alguém dotado de fé. </p>

<p class=Normal> (4) "Outra vez, ele é fácil de ser corrigido e possui qualidades que fazem com que ele seja fácil de ser corrigido; ele é paciente e recebe as instruções com respeito. Essa também é uma manifestação da fé em alguém dotado de fé. </p>

<p class=Normal> (5) "Outra vez, ele é hábil e diligente em resolver as distintas tarefas que devem ser feitas para os seus companheiros bhikkhus; ele as discerne bem de modo a arranjá-las e executá-las adequadamente. Essa também é uma manifestação da fé em alguém dotado de fé. </p>

<p class=Normal> (6) "Outra vez, ele ama o Dhamma e faz afirmações que são agradáveis, plenas com sublime alegria relativa ao Dhamma e a Disciplina. Essa também é uma manifestação da fé em alguém dotado de fé. </p>

<p class=Normal> (7) "Outra vez, ele permanece com a sua energia estimulada para o abandono de qualidades mentais prejudiciais e para a obtenção de qualidades mentais benéficas; ele é decidido, firme no seu esforço, sem se esquivar das suas responsabilidades com relação ao cultivo de qualidades mentais benéficas. Essa também é uma manifestação da fé em alguém dotado de fé. </p>

<p class=Normal> (8) "Outra vez, ele obtém de acordo com a sua vontade, sem problemas ou dificuldades, os quatro jhanas que constituem a mente superior e que proporcionam uma estada prazerosa aqui e agora. Essa também é uma manifestação da fé em alguém dotado de fé. </p>

<p class=Normal> (9) "Outra vez, ele se recorda das suas muitas vidas passadas, isto é, um nascimento, dois nascimentos, três nascimentos, quatro, cinco, dez, vinte, trinta, quarenta, cinqüenta, cem, mil, cem mil, muitos ciclos cósmicos de contração, muitos ciclos cósmicos de expansão, muitos ciclos cósmicos de contração e expansão, ‘Lá eu tinha tal nome, pertencia a tal clã, tinha tal aparência. Assim era o meu alimento, assim era a minha experiência de prazer e dor, assim foi o fim da minha vida. Falecendo daquele estado, eu ressurgi ali. Ali eu também tinha tal nome, pertencia a tal clã, tinha tal aparência. Assim era o meu alimento, assim era a minha experiência de prazer e dor, assim foi o fim da minha vida. Falecendo daquele estado, eu ressurgi aqui.’ Assim ele se recorda das suas muitas vidas passadas nos seus modos e detalhes. Essa também é uma manifestação da fé em alguém dotado de fé. </p>

<p class=Normal> (10) "Outra vez, por meio do olho divino, que é purificado e ultrapassa o humano, ele vê seres falecendo e renascendo, inferiores e superiores, bonitos e feios, afortunados e desafortunados. Ele compreende como os seres prosseguem de acordo com as suas ações desta forma: ‘Esses seres – dotados de má conduta com o corpo, linguagem e mente, que insultam os nobres, com o entendimento incorreto e realizando ações sob a influência do entendimento incorreto – com a dissolução do corpo, após a morte, renasceram num estado de privação, num destino infeliz, nos reinos inferiores, até mesmo no inferno. Porém estes seres - dotados de boa conduta com o corpo, linguagem e mente, que não insultam os nobres, com o entendimento correto e realizando ações sob a influência do entendimento correto – com a dissolução do corpo, após a morte, renasceram num destino feliz, no paraíso.’ Dessa forma - por meio do olho divino, que é purificado e ultrapassa o humano, ele vê seres falecendo e renascendo, inferiores e superiores, bonitos e feios, afortunados e desafortunados, e ele compreende como os seres prosseguem de acordo com as suas ações. Essa também é uma manifestação da fé em alguém dotado de fé. </p>

<p class=Normal> (11) "Outra vez, realizando por si mesmo através do conhecimento direto, aqui e agora, ele entra e permanece na libertação da mente e libertação através da sabedoria que são imaculadas com a destruição de todas as impurezas. Essa também é uma manifestação da fé em alguém dotado de fé. </p>

<p class=Normal> Quando isso foi dito, o venerável Subhuti disse para o Abençoado: 'Venerável senhor, essas manifestações de fé em alguém dotado de fé que o Abençoado descreveu são vistas neste bhikkhu, ele as exibe. </p>

<p class=Normal> (1) Este bhikkhu, venerável senhor, é virtuoso ... ele treina adotando os preceitos de virtude. </p>

<p class=Normal> (2) "Ele ouviu muito, ... penetrou corretamente com o entendimento. </p>

<p class=Normal> (3) "Ele tem bons amigos, companheiros, e camaradas. </p>

<p class=Normal> (4) "Ele é fácil de ser corrigido e possui qualidades que fazem com que ele seja fácil de ser corrigido; ele é paciente e recebe as instruções com respeito. </p>

<p class=Normal> (5) "Ele é hábil e diligente ... executá-las adequadamente. </p>

<p class=Normal> (6) "Ele ama o Dhamma ... relativa ao Dhamma e a Disciplina. </p>

<p class=Normal> (7) "Ele permanece com a sua energia estimulada ... cultivo de qualidades mentais benéficas. </p>

<p class=Normal> (8) "Ele obtém de acordo com a sua vontade, sem problemas ou dificuldades, os quatro jhanas que constituem a mente superior e que proporcionam uma estada prazerosa aqui e agora. </p>

<p class=Normal> (9) "Ele se recorda das suas muitas vidas passadas, ...  Assim ele se recorda das suas muitas vidas passadas nos seus modos e detalhes. </p>

<p class=Normal> (10) "Por meio do olho divino, ... ele compreende como os seres prosseguem de acordo com as suas ações. </p>

<p class=Normal> (11) "Realizando por si mesmo através do conhecimento direto, aqui e agora, ele entra e permanece na libertação da mente e libertação através da sabedoria que são imaculadas com a destruição de todas as impurezas. </p>

<p class=Normal> "Venerável senhor, essas manifestações de fé em alguém dotado de fé que o Abençoado descreveu são vistas neste bhikkhu, ele as exibe." </p>

<p class=Normal> "Muito bem, Subhuti! Nesse caso, Subhuti, você pode permanecer junto com esse bhikkhu Saddha e quando você quiser ver o Tathagata ele poderá acompanhá-lo." </p>



