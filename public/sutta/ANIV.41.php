<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
há esses quatro desenvolvimentos da concentração. Quais quatro? Há o
desenvolvimento da concentração que, quando desenvolvida e cultivada, conduz a
uma permanência prazerosa aqui e agora. Há o desenvolvimento da concentração
que, quando desenvolvida e cultivada, conduz a realizar o conhecimento e visão.
Há o desenvolvimento da concentração que, quando desenvolvida e cultivada,
conduz à atenção plena e plena consciência. Há o desenvolvimento da
concentração que, quando desenvolvida e cultivada, conduz ao fim das impurezas.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual é o desenvolvimento
da concentração que, quando desenvolvida e cultivada conduz a uma permanência
prazerosa aqui e agora? É o caso em que um bhikkhu afastado dos prazeres sensuais, afastado das qualidades não hábeis, entra e permanece no primeiro jhana, que é caracterizado pelo pensamento aplicado e sustentado, com o êxtase e felicidade nascidos do afastamento. Abandonando o pensamento aplicado e sustentado, um bhikkhu entra e permanece no segundo jhana, que é caracterizado pela segurança interna e perfeita unicidade da mente, sem o pensamento aplicado e sustentado, com o êxtase e felicidade nascidos da concentração. Abandonando o êxtase, um bhikkhu entra e permanece no terceiro jhana que é caracterizado pela felicidade sem o êxtase, acompanhada pela atenção plena, plena consciência e equanimidade, acerca do qual os nobres declaram: ‘Ele permanece numa estada feliz, equânime e plenamente atento.’Com o completo desaparecimento da felicidade, um bhikkhu entra e permanece no quarto jhana, que possui nem felicidade nem sofrimento, com a atenção plena e a equanimidade purificadas. Esse é o
desenvolvimento da concentração que ... conduz a uma permanência prazerosa aqui
e agora. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual é o desenvolvimento
da concentração que ... conduz a realizar o conhecimento e visão? É o caso em
que um bhikkhu ocupa-se com a percepção da luz e está resoluto na percepção da
luz do dia (a qualquer hora do dia). O dia (para ele) é o mesmo que a noite, a
noite o mesmo que o dia. Por meio de uma consciência livre e desimpedida, ele
desenvolve uma mente luminosa. Esse é o desenvolvimento da concentração que,
quando desenvolvida e cultivada, conduz a realizar o conhecimento e visão. [<a
href="#N1">1</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual é o
desenvolvimento da concentração que ... conduz à atenção plena e plena
consciência? É o caso em que as sensações são compreendidas quando surgem,
compreendidas enquanto estão presentes, compreendidas quando desaparecem. As percepções são compreendidas
quando surgem, compreendidas enquanto estão presentes, compreendidas quando desaparecem. Os
pensamentos são compreendidos quando surgem, compreendidos enquanto estão
presentes, compreendidos quando desaparecem. 
Esse é o desenvolvimento da concentração que, quando desenvolvida e cultivada,
conduz à atenção plena e plena consciência. [<a href="#N2">2</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual é o
desenvolvimento da concentração que ... conduz ao fim das impurezas? É o caso
em que o bhikkhu compreende a origem e cessação com relacão aos cinco agregados
influenciados pelo apego: 'Assim é a forma, essa é sua origem, essa é a sua
cessação. Assim são as sensações ... Assim são as percepções ... Assim são as
formações ... Assim é a consciência, essa é sua origem, essa é a sua cessação.'
Esse é o desenvolvimento da concentração que quando desenvolvida e cultivada,
conduz ao fim das impurezas. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Esses são os quatro
desenvolvimentos da concentração.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E foi com relação a isso que
afirmei no ‘Punnaka-manava-puccha’ [<a href="SnpV.3.php">Snp V 3</a>]: <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:PT-BR'>‘Aquele
que compreendeu no mundo o aqui e o além, em quem não há perturbação por nada
no mundo, que está tranqüilo, livre das chamas ardentes, despreocupado e livre
do desejo – ele superou o nascimento e o envelhecimento, eu digo.’” <o:p></o:p></span></p>

