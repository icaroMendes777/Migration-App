<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. </span>Assim
ouvi. Em certa ocasião o ven. <span lang=PT-BR style='mso-ansi-language:PT-BR'>Maha
Moggallana </span>estava entre os<span lang=PT-BR style='mso-ansi-language:
PT-BR'> Bhaggas em Sumsumaragira no Bosque de Bhesakala, no Parque do Gamo. </span>Lá,
ele se dirigiu aos monges desta forma: “Amigos” – “Amigo,” eles responderam. O
ven. <span lang=PT-BR style='mso-ansi-language:PT-BR'>Maha Moggallana</span>
disse o seguinte<span lang=PT-BR style='mso-ansi-language:PT-BR'>:<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. “Amigos,
embora um bhikkhu assim peça: ‘Que os veneráveis me admoestem, [<a href="#N1">1</a>]
<a name=R1></a>eu preciso ser admoestado pelos veneráveis,’ no entanto, se ele
é difícil de ser admoestado e possui qualidades que fazem com que ele seja
difícil de ser admoestado, se ele for impaciente e não aceitar a instrução da
forma correta, então os seus companheiros na vida santa pensarão que ele não
deve ser admoestado ou instruído, eles pensarão que ele é uma pessoa que não
merece confiança. <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. “Quais
qualidades fazem com que ele seja difícil de ser admoestado?<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(1) Aqui um
bhikkhu possui desejos ruins e é dominado pelos desejos ruins; [<a href="#N2">2</a>]
<a name=R2></a>essa é uma qualidade que faz com que ele seja difícil de ser
admoestado. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(2) Outra
vez, um bhikkhu elogia a si mesmo e menospreza os outros; essa é uma qualidade
que faz com que ele seja difícil de ser admoestado.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(3) Outra
vez, um bhikkhu tem raiva e é dominado pela raiva; essa é uma qualidade ...<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(4) Outra
vez, um bhikkhu tem raiva e é vingativo devido à raiva ...<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(5) Outra
vez, um bhikkhu tem raiva e é teimoso devido à raiva ...<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(6) Outra
vez, um bhikkhu tem raiva e diz palavras próximas à raiva ...<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(7) Outra
vez, um bhikkhu é reprovado e resiste ao reprovador ... <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(8) Outra
vez, um bhikkhu é reprovado e denigre o reprovador ... <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(9) Outra
vez, um bhikkhu é reprovado e contra-reprova o reprovador ... <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(10) Outra
vez, um bhikkhu é reprovado e mente usa subterfúgios e demonstra raiva, ódio e
amargor ... <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(11) Outra
vez, um bhikkhu é reprovado e deixa de se responsabilizar pela sua conduta ...<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(12) Outra
vez, um bhikkhu é desprezativo ou insolente ... <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(13) Outra
vez, um bhikkhu é invejoso ou avarento ...<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(14) Outra
vez, um bhikkhu é dissimulador ou trapaçeiro ...<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(15) Outra
vez, um bhikkhu é teimoso ou arrogante ...<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(16) Outra vez,
um bhikkhu tem apego às suas idéias, ele as agarra com tenacidade e as abandona
com dificuldade;<span style="mso-spacerun: yes">  </span>essa é uma qualidade
que faz com que ele seja difícil de ser admoestado.[<a href="#N3">3</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Amigos, essas são chamadas as qualidades que fazem com que ele seja
difícil de ser admoestado.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. “Amigos,
embora um bhikkhu assim não peça: ‘Que os veneráveis me admoestem, eu preciso
ser admoestado pelos veneráveis,’ no entanto, se ele é fácil de ser admoestado
e possui qualidades que fazem com que ele seja fácil de ser admoestado, se ele
for paciente e aceitar a instrução da forma correta, então os seus companheiros
na vida santa pensarão que ele deve ser admoestado e instruído, eles pensarão
que ele é uma pessoa que merece confiança.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. “Quais
qualidades fazem com que ele seja fácil de ser admoestado?<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(1) Aqui um
bhikkhu não possui desejos ruins e não é dominado pelos desejos ruins; essa é uma
qualidade que faz com que ele seja fácil de ser admoestado.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(2) Outra
vez, um bhikkhu não elogia a si mesmo nem menospreza os outros; essa é uma
qualidade ...<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(3) Outra
vez, um bhikkhu não
tem raiva e não permite que a raiva o domine ...<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(4) Outra
vez, um bhikkhu não
tem raiva nem é vingativo devido à raiva ...<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(5) Outra
vez, um bhikkhu não
tem raiva nem é teimoso devido à raiva ...<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(6) Outra
vez, um bhikkhu não
tem raiva e ele não diz palavras próximas à raiva ...<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(7) Outra
vez, um bhikkhu é
reprovado e não resiste ao reprovador ... <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(8) Outra
vez, um bhikkhu é
reprovado e não denigre o reprovador ...<span style="mso-spacerun: yes"> 
</span><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(9) Outra
vez, um bhikkhu é
reprovado e não contra-reprova o reprovador ... <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(10) Outra
vez, um bhikkhu é
reprovado e não mente não usa subterfúgios e não demonstra raiva, ódio e
amargor... <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(11) Outra
vez, um bhikkhu é
reprovado e não deixa de se responsabilizar pela sua conduta ...<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(12) Outra
vez, um bhikkhu
não é desprezativo ou insolente ... <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(13) Outra
vez, um bhikkhu
não é invejoso ou avarento ...<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(14) Outra
vez, um bhikkhu
não é dissimulador ou trapaçeiro ...<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(15) Outra
vez, um bhikkhu
não é teimoso ou arrogante ...<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(16) Outra
vez, um bhikkhu não tem apego às suas idéias, ele não as agarra com tenacidade,
mas as abandona com facilidade; essa é uma qualidade que faz com que ele seja
fácil de ser admoestado.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigos,
essas são chamadas as qualidades que fazem com que ele seja fácil de ser
admoestado.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. “Agora,
amigos, um bhikkhu deve inferir acerca de si mesmo da seguinte forma: [<a
href="#N4">4</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>(1) ‘Uma pessoa que possui desejos ruins e é dominada pelos desejos ruins
é irritante e desagradável para mim. Se eu tivesse desejos ruins e fosse
dominado pelos desejos ruins, eu seria irritante e desagradável para os outros.’
Um bhikkhu que sabe disso deveria incitar a sua mente assim: ‘Eu não terei desejos ruins 
e não serei dominado pelos desejos ruins.’<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(2-16) 'Uma
pessoa que elogia a si mesma e menospreza os outros ... uma pessoa que tem
apego às suas idéias,<span style="mso-spacerun: yes">  </span>que as agarra com
tenacidade e que as abandona com dificuldade é irritante e desagradável para
mim. Se eu tivesse apego às minhas idéias, as agarrasse com tenacidade e as abandonasse com dificuldade, eu
seria irritante e desagradável para os outros.’ Um bhikkhu que sabe disso
deveria incitar a sua mente assim: ‘Eu não me apegarei às minhas idéias,
agarrando-as com tenacidade, mas abandoná-las-ei com facilidade.’<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. “Agora, amigos,
um bhikkhu deveria analisar a si mesmo assim:<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(1) ‘Eu
tenho desejos ruins e sou dominado pelos desejos ruins?’ Se, ao investigar a si
mesmo, ele sabe que: ‘Eu tenho desejos ruins, eu estou dominado pelos 
desejos ruins,’ então, ele deveria fazer o esforço para abandonar esses estados ruins
e prejudiciais. Mas, se ao investigar a si mesmo, ele sabe que: ‘Eu não tenho
 desejos ruins, eu não estou dominado pelos desejos ruins,’ então, ele deve
permanecer feliz e contente, treinando dia e noite nos estados benéficos.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(2-16)
Outra vez, um bhikkhu deveria analisar a si mesmo assim: ‘Eu elogio a mim mesmo
e menosprezo os outros?’ ... ‘Eu tenho apego às minhas idéias, agarrando-as com
tenacidade e abandonando-as com dificuldade?’ Se, ao investigar a si mesmo, ele
sabe que: ‘Eu tenho apego às minhas idéias ...,’ então ele deveria fazer o
esforço para abandonar esses estados ruins e prejudiciais. Mas se, ao
investigar a si mesmo, ele sabe que: ‘Eu não tenho apego às minhas idéias ...,’
então ele deve permanecer feliz e contente, treinando dia e noite nos estados
benéficos.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. “Amigos,
quando um bhikkhu analisa a si mesmo dessa forma, se ele vir que esses estados
ruins e prejudiciais não foram todos abandonados por ele, então ele deve fazer
o esforço para abandoná-los todos. Mas, se ao analisar a si mesmo dessa forma,
ele vir que todos<span style="mso-spacerun: yes">  </span>foram abandonados por
ele, então ele deve permanecer feliz e contente, treinando dia e noite nos
estados benéficos.[<a href="#N5">5</a>]<o:p></o:p></span></p>


<p class=Normal><a name=R5></a>Tal como uma mulher, ou um homem, jovem,
sadio, que aprecia ornamentos<span lang=PT-BR style='mso-ansi-language:PT-BR'>,
ao ver a imagem do rosto refletida num espelho límpido e brilhante ou numa
bacia com água cristalina, percebe uma mancha ou defeito, fará esforço para
removê-la, mas não percebendo mancha ou defeito ficará<span style="mso-spacerun:
yes">  </span>feliz assim: ‘É um ganho para mim que esteja perfeito’; do mesmo
modo, quando um bhikkhu analisa a si mesmo dessa forma ... então ele deve
permanecer feliz e contente, treinando dia e noite nos estados benéficos.”<o:p></o:p></span></p>


<p class=Normal>Isso foi o que disse o ven. <span lang=PT-BR
style='mso-ansi-language:PT-BR'>Maha Moggallana</span>. Os bhikkhus ficaram
satisfeitos e contentes com as palavras do ven. <span lang=PT-BR
style='mso-ansi-language:PT-BR'>Maha Moggallana.<o:p></o:p></span></p>


