<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
há esses quatro jhanas. Quais quatro? Neste caso, bhikkhus, afastado dos prazeres
sensuais, afastado das qualidades não hábeis, um bhikkhu entra e permanece no
primeiro jhana, que é acompanhado pelo pensamento aplicado e sustentado, com o
êxtase e felicidade nascidos do afastamento. Silenciando o pensamento aplicado
e sustentado, um bhikkhu entra e permanece no segundo jhana, que é acompanhado
pela autoconfiança e unicidade da mente sem o pensamento aplicado e sustentado,
com o êxtase e felicidade nascidos da concentração. Com o desaparecer do
êxtase, um bhikkhu permanece com a equanimidade, plenamente atento e plenamente
consciente, ainda sentindo felicidade no corpo, ele entra e permanece no
terceiro jhana, acerca do qual os nobres declaram: ‘Possui uma estada feliz
aquele que é equânime e plenamente atento.’ Com o abandono da felicidade e do
sofrimento, e com o anterior desaparecimento da alegria e tristeza, um bhikkhu
entra e permanece no quarto jhana, que possui nem felicidade, nem sofrimento e
a atenção plena purificada devido à equanimidade. Esses são os quatro jhanas.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
como<span style="mso-spacerun: yes">  </span>o rio Gânges decliva, tende e se
inclina na direção do oceano, um bhikkhu que desenvolve e cultiva os quatro
jhanas também decliva, tende e se inclina na direção de Nibbana.[<a href="#N1">1</a>]<o:p></o:p></span></p>


<p class=Normal><a name=R2></a><a name=R1></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>“E como, bhikkhus, um bhikkhu desenvolve e
cultiva os quatro jhanas para ele declivar, tender e se inclinar na direção de
Nibbana? Neste caso, bhikkhus, afastado dos prazeres sensuais, afastado das
qualidades não hábeis, um bhikkhu entra e permanece no primeiro jhana ... no
segundo jhana ... no terceiro jhana ... no quarto jhana. <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“É dessa
forma, bhikkhus, que um bhikkhu desenvolve e cultiva os quatro jhanas para ele
declivar, tender e se inclinar na direção de Nibbana.”<o:p></o:p></span></p>


