<p class=Normal> &nbsp;Então um certo bhikkhu foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse: &quot;Seria bom
se o Abençoado pudesse me ensinar o Dhamma de maneira resumida, de modo que,
tendo ouvido o Dhamma do Abençoado, eu possa permanecer só, isolado, diligente, ardente e decidido.&quot; <o:p></o:p></span></p>


<p class=Normal> &quot;Mas é justamente dessa maneira que alguns homens tolos fazem um
pedido e então, tendo ouvido o Dhamma, pensam que devem seguir-me aonde quer
que eu vá.&quot; <o:p></o:p></span></p>


<p class=Normal>&quot;Possa o Abençoado ensinar-me o Dhamma de maneira resumida! Possa
o Iluminado ensinar-me o Dhamma de maneira resumida! É bem possível que eu
entenda as palavras do Abençoado. É bem possível que eu me torne um herdeiro
das palavras do Abençoado.&quot; <o:p></o:p></span></p>


<p class=Normal> &quot;Então, bhikkhu, você deve treinar assim: 'Minha mente será
bem estabelecida internamente, imperturbável. Nenhuma qualidade ruim e prejudicial, uma
vez que tenha surgido, permanecerá consumindo a mente.' Assim é como você deve treinar.
<o:p></o:p></span></p>


<p class=Normal> &quot;Então, você deve treinar assim: 'O amor bondade, como libertação
da minha mente, será desenvolvido e cultivado, farei dele o meu veículo, a minha
base, estabilizá-lo-ei, me exercitarei nele e o aperfeiçoarei.' Assim é como
você deve treinar. Tendo desenvolvido a concentração dessa forma, você deve
desenvolver essa concentração com o pensamento aplicado e sustentado, você deve
desenvolvê-la sem o pensamento aplicado e com uma pequena dose de pensamento
sustentado, você deve desenvolvê-la sem o pensamento aplicado e sem o
pensamento sustentado, você deve desenvolvê-la acompanhada de êxtase ... não
acompanhada de êxtase ... dotada de felicidade; você deve desenvolvê-la dotada
de equanimidade. <o:p></o:p></span></p>


<p class=Normal> &quot;Quando essa concentração é assim desenvolvida, bem desenvolvida
dessa forma por você, então você deve treinar assim: 'Compaixão, como
libertação da minha mente ... Alegria altruísta, como libertação da minha
mente<span style="mso-spacerun: yes">  </span>... Equanimidade, como libertação
da minha mente, será desenvolvida e cultivada, farei dela o meu veículo, a minha
base, estabilizá-la-ei, me exercitarei nela e a aperfeiçoarei.'<span
style="mso-spacerun: yes">  </span>Assim é como você deve treinar. Tendo
desenvolvido a concentração dessa forma, você deve desenvolver essa
concentração com o pensamento aplicado e sustentado, você deve desenvolvê-la
sem o pensamento aplicado e com uma pequena dose de pensamento sustentado, você
deve desenvolvê-la sem o pensamento aplicado e sem o pensamento sustentado,
você deve desenvolvê-la acompanhada de êxtase ... não acompanhada de êxtase ...
dotada de felicidade; você deve desenvolvê-la dotada de equanimidade. <o:p></o:p></span></p>

<p class=Normal> &quot;Quando essa concentração é assim desenvolvida, bem desenvolvida
dessa forma por você, então você deve treinar assim: 'Eu permanecerei contemplando o
corpo como um corpo, ardente, plenamente consciente e com atenção plena, tendo colocado de lado a cobiça e o desprazer pelo mundo.'<span style="mso-spacerun: yes"> 
</span>Assim é como você deve treinar. Tendo desenvolvido a concentração dessa
forma, você deve desenvolver essa concentração com o pensamento aplicado e
sustentado, você deve desenvolvê-la sem o pensamento aplicado e com uma pequena
dose de pensamento sustentado, você deve desenvolvê-la sem o pensamento
aplicado e sem o pensamento sustentado, você deve desenvolvê-la acompanhada de
êxtase ... não acompanhada de êxtase ... dotada de felicidade; você deve
desenvolvê-la dotada de equanimidade. <o:p></o:p></span></p>


<p class=Normal> &quot;Quando a concentração é desenvolvida dessa forma, bem
desenvolvida dessa forma por você, então você deve treinar assim: 'Eu
permanecerei contemplando as sensações ... a mente ... os objetos mentais como objetos mentais, ardente, plenamente consciente e com atenção plena, tendo colocado de lado a cobiça e o desprazer pelo mundo.'<span style="mso-spacerun: yes">  </span>Assim
é como você deve treinar. Tendo desenvolvido a concentração dessa forma, você
deve desenvolver essa concentração com o pensamento aplicado e sustentado, você
deve desenvolvê-la sem o pensamento aplicado e com uma pequena dose de
pensamento sustentado, você deve desenvolvê-la sem o pensamento aplicado e sem
o pensamento sustentado, você deve desenvolvê-la acompanhada de êxtase ... não
acompanhada de êxtase ... dotada de felicidade; você deve desenvolvê-la dotada
de equanimidade. <o:p></o:p></span></p>


<p class=Normal> &quot;Quando a concentração é desenvolvida dessa forma, bem
desenvolvida dessa forma por você, então onde quer que você vá, você irá com
conforto. Onde quer que você fique em pé, você ficará em pé com conforto. Onde
quer que você<span style="mso-spacerun: yes">  </span>sente, você sentará com
conforto. Onde quer que você deite, você deitará com conforto.&quot; <o:p></o:p></span></p>


<p class=Normal> Então esse bhikkhu, tendo sido exortado pelo Abençoado, levantou do
seu assento e depois de homenagear o Abençoado, mantendo-o à sua direita,
partiu. Permanecendo só,
isolado, diligente, ardente e decidido, em pouco tempo, ele alcançou e
permaneceu no objetivo supremo da vida santa pelo qual membros de um clã deixam
a vida em família pela vida santa, tendo conhecido e realizado por si mesmo no
aqui e agora. Ele soube: “O nascimento foi destruído, a vida santa foi vivida,
o que deveria ser feito foi feito, não há mais vir a ser a nenhum estado.” E
assim ele tornou-se mais um dos Arahants.<o:p></o:p></span></p>


