<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim ouvi.
Em certa ocasião o Abençoado estava em Benares, no Parque do Gamo, em
Isipatana. Lá ele se dirigiu aos monges desta forma: “Bhikkhus” – “Venerável
Senhor,” eles responderam. O Abençoado disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><a name=T2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2. “Em Benares, bhikhhus, no Parque do Gamo em Isipatana o Tathagata,
um arahant, perfeitamente iluminado, colocou em movimento a incomparável Roda do
Dhamma, [<a href="#N1">1</a>] <a name=R1></a>que não pode ser parada por nenhum
contemplativo ou brâmane ou deva ou Mara ou Brahma ou ninguém no mundo – isto
é, o anúncio, ensino, descrição, estabelecimento, revelação, exposição e
demonstração das Quatro Nobres Verdades. Quais quatro?<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. “O anúncio, ensino,
descrição, estabelecimento, revelação, exposição e demonstração<span
style='mso-spacerun:yes'>  </span>da nobre verdade do sofrimento. O anúncio, ensino,
descrição, estabelecimento, revelação, exposição e demonstração da nobre
verdade da origem do sofrimento ... da nobre verdade da cessação do sofrimento
... da nobre verdade do caminho que conduz à cessação do sofrimento.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. “Em Benares, bhikkhus,
no Parque do Gamo em Isipatana o Tathagata, um arahant, perfeitamente iluminado,
colocou em movimento a incomparável Roda do Dhamma, que não pode ser parada por
nenhum contemplativo ou brâmane ou deva ou Mara ou Brahma ou ninguém no<span
style='mso-spacerun:yes'>  </span>mundo – isto é, o anúncio, ensino, descrição,
estabelecimento, revelação, exposição e demonstração dessas Quatro Nobres
Verdades.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. “Cultivem a amizade de
Sariputta e Mogallana bhikkhus; associem-se a Sariputta e Mogallana. Eles são
sábios e auxiliam os seus companheiros na vida santa. Sariputta é tal como uma
mãe; Mogallana é tal como uma enfermeira. Sariputta treina os companheiros para
alcançar o fruto de ‘entrar na correnteza’, Mogallana para alcançar o objetivo
máximo. [<a href="#N2">2</a>]<a name=R2></a> Sariputta bhikkhus, é capaz de anunciar,
ensinar, descrever, estabelecer, revelar, expor e demonstrar as Quatro Nobres
Verdades.”<span style='mso-spacerun:yes'>  </span></span><span lang=PT-BR
style='font-size:11.0pt;mso-bidi-font-size:12.0pt;mso-ansi-language:PT-BR'><o:p></o:p></span></p>

<p class=Normal>6. </span><span
lang=PT-BR style='mso-ansi-language:PT-BR'>Assim falou o Abençoado. Tendo dito
isso, o Sublime levantou-se do seu assento e foi para o seu retiro.</span><span
lang=PT-BR style='font-size:11.0pt;mso-bidi-font-size:12.0pt;mso-ansi-language:
PT-BR'> <o:p></o:p></span></p>

<p class=Normal>7. </span><span lang=PT-BR
style='mso-ansi-language:PT-BR'>Então, pouco tempo depois do Abençoado ter ido,
o venerável Sariputta dirigiu-se aos bhikkhus desta forma: “Amigos bhikkhus” –
“Amigo,” os bhikkhus responderam ao venerável Sariputta. O venerável Sariputta
disse o segui<a name=R6></a>nte:<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8.
“Em Benares amigos, no Parque do Gamo em Isipatana o Tathagata, um arahant, perfeitamente
iluminado, colocou em movimento a incomparável Roda do Dhamma....e demonstração
das Quatro Nobres Verdades. Quais quatro? <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9.
“O anúncio...e a demonstração da nobre verdade do sofrimento...da nobre verdade
da origem do sofrimento...da nobre verdade da cessação do sofrimento...da nobre
verdade do caminho que conduz à cessação do sofrimento. <o:p></o:p></span></p>

<p class=Normal><a name=T10></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>10. “E o que, amigos, é a nobre verdade do
sofrimento? O nascimento é sofrimento; o envelhecimento é sofrimento; a morte é
sofrimento; tristeza, lamentação, dor, angústia e desespero são sofrimento; não
obter o que se deseja é sofrimento; em resumo, os cinco agregados influenciados
pelo apego são sofrimento. </span><span lang=PT-BR style='font-size:11.0pt;
mso-bidi-font-size:12.0pt;mso-ansi-language:PT-BR'><span
style='mso-spacerun:yes'> </span></span><span lang=PT-BR style='mso-ansi-language:
PT-BR'><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11.
“E o que, amigos, é nascimento? [<a href="#N3">3</a>]<a name=R3></a> O
nascimento dos seres nas várias classes de seres, o próximo nascimento, o
estabelecimento [num ventre], a geração, a manifestação dos agregados, a
obtenção das bases para contato – a isto se denomina nascimento.</span><span
lang=PT-BR style='font-size:11.0pt;mso-bidi-font-size:12.0pt;mso-ansi-language:
PT-BR'> <a name=R10></a><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12.
“E o que, amigos, é o envelhecimento? O envelhecimento dos seres nas várias
categorias de seres, a sua<a name=R11></a> idade avançada, os dentes
quebradiços, os cabelos grisalhos, a pele enrugada, o declínio da vida, o
enfraquecimento das faculdades – a isto se denomina envelhecimento.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. “E o que, amigos, é a
morte? O falecimento de seres nas várias categorias de se<a name=R13></a>res, a
sua morte, a dissolução, o desaparecimento, o morrer, a finalização do tempo, a
dissolução dos agregados, o cadáver descartado – a isto de denomina morte. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14. “E o que, amigos, é a
tristeza? A tristeza, entristecimento, sofrimento, tristeza interior,
arrependimento interior, de alguém que sofreu alguma desgraça ou que está
afetado por alguma situação dolorosa – a isto se denomina tristeza. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. “E o que, amigos, é a
lamentação? O pranto e o lamento, chorar e lamentar, o choro e a lamentação de
alguém que sofreu alguma desgraça ou que está afetado por alguma situação
dolorosa – a isto se denomina lamentação. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16. “E o que, amigos, é a
dor? Dor no corpo, desconforto corporal, a sensação dolorosa e desconfortável
que surge do contato corporal – a isto se denomina dor. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17. “E o que, amigos, é a
angústia? Dor mental, desconforto mental, a sensação dolorosa e desconfortável
que surge do contato mental – a isto se denomina angústia. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18. “E o que, amigos, é o
desespero? A confusão e o desespero, a tribulação e a desesperação de alguém
que sofreu alguma desgraça ou que está afetado por alguma situação dolorosa – a
isto se denomina desespero.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>19. “E o que, amigos, é
‘não obter o que se deseja é sofrimento’? Para os seres sujeitos ao nascimento
surge o desejo: ‘Ah, que nós não estivéssemos sujeitos ao nascimento! Que o
nascimento não viesse para nós!’ Mas isto não pode ser obtido pelo desejo e não
obter o que se deseja é sofrimento. Para os seres sujeitos ao
envelhecimento ... sujeitos à enfermidade ... sujeitos à morte ... sujeitos à
tristeza, lamentação, dor, angústia e desespero, surge o des<a name=R14></a>ejo:
‘Ah, que nós não estivéssemos sujeitos à tristeza, lamentação, dor, angústia e
desespero! Que a tristeza, lamentação, dor, angústia e desespero não surgissem
para nós!’ Mas isto não pode ser obtido pelo desejo e não obter o que se deseja
é sofrimento. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>20. “E o que, amigos são os
cinco agregados influenciados pelo apego que, em resumo, são sofrimento? Eles
são: o agregado da forma material influenciado pelo apego, o agregado da
sensação influenciado pelo apego, o agregado da percepção influenciado pelo
apego, o agregado das formações influenciado pelo apego e o agregado da
consciência influenciado pelo apego. Esses são os cinco agregados influenciados
pelo apego que, em resumo, são sofrimento. A isto se denomina a nobre verdade
do sofrimento.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>21. “ E o que, amigos, é a
nobre verdade da origem do sofrimento? É o desejo, que conduz a uma renovada existência, 
acompanhado pela cobiça e pelo prazer, buscando o prazer aqui e ali; isto é, o desejo pelos prazeres 
sensuais, o desejo por ser/existir, o desejo por não ser/existir. A isto se
denomina a nobre verdade da origem do sofrimento.<span
style='mso-spacerun:yes'>  </span><o:p></o:p></span></p>

<p class=Normal><a name=R17></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>22. “ E o
que, amigos, é a nobre verdade da cessação do sofrimento? É o desaparecimento e cessação sem deixar 
nenhum vestígio daquele mesmo desejo, abrir mão, descartar, libertar-se, despegar desse mesmo desejo. 
A isto se denomina a nobre verdade da cessação do sofrimento. [<a href="#N4">4</a>] <a name=R4></a><o:p></o:p></span></p>

<p class=Normal><a name=R4></a><a name=T23></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>23. “ E o
que, amigos, é a nobre verdade do caminho que conduz à cessação do sofrimento?
É exatamente este Nobre Caminho Óctuplo; isto é, entendimento correto,
pensamento correto, linguagem correta, ação correta, modo de vida correto,
esforço correto, atenção plena correta e concentração correta.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>24 “ E o que, amigos, é
entendimento correto? Entendimento do sofrimento, entendimento da origem do
sofrimento, entendimento da cessação do sofrimento e entendimento do caminho
que conduz à cessação do sofrimento – a isto se denomina entendimento correto.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>25. “ E o que, amigos, é
pensamento correto? O pensamento da renúncia, o pensamento da não má vontade e
o pensamento da não crueldade – a isto se denomina pensamento correto.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>26. “ E o que, amigos, é
linguagem correta? Abster-se da linguagem mentirosa, abster-se da linguagem
maliciosa, abster-se da linguagem grosseira e abster-se de linguagem frívola –
a isto se denomina linguagem correta.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>27. “ E o que, amigos, é
ação correta? Abster-se de matar seres vivos, abster-se de tomar o que não seja
dado e abster-se de conduta imprópria com relação aos prazeres sensuais – a
isto se denomina ação correta.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>28. “ E o que, amigos, é
modo de vida correto? Aqui um nobre discípulo, tendo abandonado o modo de vida
incorreto, ganha o seu pão através do modo de vida correto – a isto se denomina
modo de vida correto.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>29 “ E o que, amigos, é
esforço correto? Aqui um bhikkhu gera desejo para que não surjam estados ruins
e prejudiciais que ainda não surgiram e ele se aplica, estimula a sua energia,
empenha a sua mente e se esforça. Ele gera desejo em abandonar estados ruins e prejudiciais
que já surgiram e ele se aplica, estimula a sua energia, empenha a sua mente e
se esforça. Ele gera desejo para que surjam estados benéficos que ainda não
surgiram e ele se aplica, estimula a sua energia, empenha a sua mente e se
esforça. Ele gera desejo para a continuidade, o não desaparecimento, o
fortalecimento, o incremento e a realização através do desenvolvimento de
estados benéficos que já surgiram e ele se aplica, estimula a sua energia,
empenha a sua mente e se esforça. A isto se denomina esforço correto.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>30. “ E o que amigos, é
atenção plena correta? Aqui um bhikkhu permanece contemplando o corpo como um
corpo, ardente, plenamente consciente e com atenção plena, tendo colocado de
lado a cobiça e o desprazer pelo mundo. Ele permanece contemplando as sensações
como sensações, ardente, plenamente consciente e com atenção plena, tendo
colocado de lado a cobiça e o desprazer pelo mundo. Ele permanece contemplando
a mente como mente, ardente, plenamente consciente e com atenção plena, tendo colocado
de lado a cobiça e o desprazer pelo mundo. Ele permanece contemplando os
objetos mentais como objetos mentais, ardente, plenamente consciente e com
atenção plena, tendo colocado de lado a cobiça e o desprazer pelo mundo. A isto
se denomina atenção plena correta.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>31. “ E o que, amigos, é
concentração correta? Aqui, um bhikkhu afastado dos prazeres sensuais, afastado das qualidades não hábeis, entra e permanece no primeiro jhana, que é caracterizado pelo pensamento aplicado e sustentado, com o êxtase e felicidade nascidos do afastamento. Abandonando o pensamento aplicado e sustentado, um bhikkhu entra e permanece no segundo jhana, que é caracterizado pela segurança interna e perfeita unicidade da mente, sem o pensamento aplicado e sustentado, com o êxtase e felicidade nascidos da concentração. Abandonando o êxtase, um bhikkhu entra e permanece no terceiro jhana que é caracterizado pela felicidade sem o êxtase, acompanhada pela atenção plena, plena consciência e equanimidade, acerca do qual os nobres declaram: ‘Ele permanece numa estada feliz, equânime e plenamente atento.’ Com o completo desaparecimento da felicidade, um bhikkhu entra e permanece no quarto jhana, que possui nem felicidade nem sofrimento, com a atenção plena e a equanimidade purificadas. A isto se denomina concentração
correta.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“A isto se denomina a nobre
verdade do caminho que conduz à cessação do sofrimento.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>32. “Em Benares, amigos, no
Parque do Gamo em Isipatana o Tathagata, um arahant, perfeitamente iluminado,
colocou em movimento a incomparável Roda do Dhamma, que não pode ser parada por
nenhum contemplativo ou brâmane ou deva ou Mara ou Brahma ou ninguém no<span
style='mso-spacerun:yes'>  </span>mundo – isto é, o anúncio, ensino, descrição,
estabelecimento, revelação, exposição e demonstração dessas Quatro Nobres
Verdades.”<o:p></o:p></span></p>

<p class=Normal><a name=R18></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>Isso foi o que disse o venerável Sariputta. Os
bhikkhus ficaram satisfeitos e contentes com as palavras do venerável
Sariputta.<o:p></o:p></span></p>

