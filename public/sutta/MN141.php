<p class=Tit3 align=center style='text-align:center'><b> Majjhima Nikaya 141 </b></Tit3>
<p class=Tit1 align=center style='text-align:center'>Saccavibhanga Sutta </Tit1>
<p class=Tit1 align=center style='text-align:center'>A Análise das Verdades </Tit1>

