<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, ao
amanhecer, o Venerável Ananda se vestiu e tomando a tigela e o manto externo,
ele foi até a moradia das bhikkhunis e sentou num assento que havia sido
preparado. Então,<span style="mso-spacerun: yes">  </span>inúmeras bhikkhunis
foram até o Venerável Ananda e depois de cumprimentá-lo sentaram a um lado e
disseram:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
Venerável Ananda, inúmeras bhikkhunis permanecem com as suas mentes bem
estabelecidas nos quatro fundamentos da atenção plena, percebendo estágios
sucessivos de elevada distinção.” [<a href="#N1">1</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Assim é, irmãs, assim é! É de se esperar que qualquer um, irmãs – quer
seja bhikkhu ou bhikkhuni – que permaneça com a mente bem estabelecida nos
quatro fundamentos da atenção plena, perceba estágios sucessivos de elevada
distinção.” Então, o Venerável Ananda, instruiu, motivou, estimulou e encorajou
aquelas bhikkhunis com um discurso do Dhamma e depois ele se levantou do seu
assento e partiu. Então, o Venerável Ananda foi para Vesali para esmolar
alimentos. Depois de haver esmolado em Vesali e de haver retornado, após a
refeição, ele foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e
relatou tudo que havia ocorrido. [O Abençoado disse:]<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Assim é,
Ananda, assim é! É de se esperar que qualquer um, Ananda – quer seja bhikkhu ou
bhikkhuni – que permaneça com a mente bem estabelecida nos quatro fundamentos
da atenção plena, perceba estágios sucessivos de elevada distinção.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quais
quatro? Neste caso, Ananda, um bhikkhu permanece contemplando o corpo como um
corpo, ardente, plenamente consciente e com atenção plena, tendo colocado de
lado a cobiça e o desprazer pelo mundo. Enquanto ele está contemplando o corpo
como um corpo, surge nele, baseado no corpo, quer seja uma febre no corpo ou
letargia mental ou a mente dele é distraída no exterior. Aquele bhikkhu deveria
então dirigir a mente para um sinal inspirador. [<a href="#N2">2</a>] <a
name=R2></a>Quando ele dirige a mente para algum sinal inspirador, a satisfação
surge. Quando há satisfação, o êxtase surge. Quando a mente é alçada pelo êxtase, o corpo 
fica tranqüilo. Com o corpo tranqüilo, ele experimenta a felicidade. A mente daquele 
que sente felicidade, se torna concentrada. Ele pensa assim:
‘O propósito pelo qual dirigi a minha mente foi alcançado. Agora irei
afastá-la.’ [<a href="#N3">3</a>] <a name=R3></a>Assim, ele afasta a mente e
não aplica ou sustenta o pensamento. Ele compreende: ‘Sem o pensamento aplicado
e sustentado, com atenção plena, eu sinto felicidade.’ [<a href="#N4">4</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Outra vez, um bhikkhu permanece contemplando as sensações como
sensações ... mente como mente ... objetos mentais como objetos mentais,
ardente, plenamente consciente e com atenção plena, tendo colocado de lado a
cobiça e o desprazer pelo mundo. Enquanto ele está contemplando os objetos
mentais como objetos mentais, surge nele, baseado nos objetos mentais, quer
seja uma febre no corpo ou letargia mental ou a mente dele é distraída no
exterior. Aquele bhikkhu deveria então dirigir a mente para um sinal
inspirador. Quando ele dirige a mente para algum sinal inspirador ... Ele
compreende: ‘Sem o pensamento aplicado e sustentado, com atenção plena, eu sinto felicidade.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“É desse
modo, Ananda, que há a meditação dirigida.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como,
Ananda, há a meditação não dirigida? Não dirigindo a mente para o
exterior, um bhikkhu compreende: ‘A minha mente não está dirigida para o
exterior.’ Então ele compreende: ‘Ela está sem limitações depois e antes,
libertada, não dirigida.’ [<a href="#N5">5</a>] <a name=R5></a>Além disso, ele
entende: ‘Eu permaneço contemplando o corpo como um corpo, ardente, plenamente
consciente e com atenção plena; eu sinto felicidade.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não
dirigindo a mente para o exterior, um bhikkhu compreende: ‘A minha mente
não está dirigida para o exterior.’ Então ele compreende: ‘Ela está sem
limitações depois e antes, libertada, não dirigida.’ Além disso, ele entende:
‘Eu permaneço contemplando as sensações como sensações, ardente, plenamente
consciente e com atenção plena; eu sinto felicidade.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não
dirigindo a mente ao exterior, um bhikkhu compreende: ‘A minha mente não está
dirigida ao exterior.’ Então ele compreende: ‘Ela está sem limitações depois e
antes, libertada, não dirigida.’ Além disso ele entende: ‘Eu permaneço
contemplando a mente como mente, ardente, plenamente consciente e com atenção
plena; eu sinto felicidade.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não
dirigindo a mente para o exterior, um bhikkhu compreende: ‘A minha mente não
está dirigida para o exterior.’ Então ele compreende: ‘Ela está sem limitações
depois e antes, libertada, não dirigida.’ Além disso, ele entende: ‘Eu
permaneço contemplando os objetos mentais como objetos mentais, ardente,
plenamente consciente e com atenção plena; eu sinto felicidade.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“É desse
modo, Ananda, que há a meditação não dirigida.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
Ananda, eu ensinei a meditação dirigida, eu ensinei a meditação não
dirigida. Aquilo que por compaixão um Mestre<span style="mso-spacerun: yes"> 
</span>deveria fazer para os seus discípulos, desejando o bem estar deles, isso
eu fiz por você, Ananda. Ali estão aquelas árvores, aquelas cabanas
vazias. Medite, Ananda, não adie ou então você irá se arrepender mais tarde.
Essa é a nossa instrução para você.&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o
que disse o Abençoado. O Venerável Ananda ficou satisfeito e contente com as
palavras do Abençoado.<o:p></o:p></span></p>


