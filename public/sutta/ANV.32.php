<p class=Normal><a name=R1></a> Certa ocasião o Abençoado estava em Rajagaha, no Bambual no Santuário dos Esquilos. Então a Princesa Cundi, [<a href="#N1">1</a>] acompanhada por quinhentas carruagens e quinhentas acompanhantes da corte foi até o Abençoado e depois de cumprimentá-lo ela sentou a um lado e disse para o Abençoado: </p>

<p class=Normal>"Venerável senhor, meu irmão é o Príncipe Cunda. Ele diz o seguinte: 'Sempre que um homem ou uma mulher busquem refúgio no Buda, no Dhamma e na Sangha, e se abstenha de matar seres vivos, de tomar aquilo que não for dado, da conduta sexual imprópria, da linguagem mentirosa, e de tomar vinho, álcool e outros embriagantes que causam negligência, com a dissolução do corpo, após a morte, ele renasce apenas num destino feliz, não num destino ruim.' Eu pergunto ao Abençoado: 'Que tipo de mestre alguém deve depositar confiança, de modo que com a dissolução do corpo, após a morte, ele renasça apenas num destino feliz, não num destino ruim? Que tipo de Dhamma alguém deve depositar confiança, de modo que com a dissolução do corpo, após a morte, ele renasça apenas num destino feliz, não num destino ruim? Que tipo de Sangha alguém deve depositar confiança, de modo que com a dissolução do corpo, após a morte, ele renasça apenas num destino feliz, não num destino ruim? Que tipo de comportamento virtuoso alguém deve observar, de modo que com a dissolução do corpo, após a morte, ele renasça apenas num destino feliz, não num destino ruim?" </p>

<p class=Normal> (1) "Cundi, entre todos os seres que possam existir – sem patas, com duas patas, quatro patas, muitas patas; com forma ou sem forma; perceptivos, não perceptivos, nem perceptivos nem não perceptivos – o Tathagata, um arahant, perfeitamente iluminado, é considerado supremo. Aqueles que têm fé no Iluminado depositam fé naquilo que é supremo; e para aqueles que têm fé no que é supremo, supremo será o resultado. </p>

<p class=Normal> (2) “Entre todos os fenômenos que são condicionados, o Nobre Caminho Óctuplo - entendimento correto, pensamento correto, linguagem correta, ação correta, modo de vida correto, esforço correto, atenção plena correta, concentração correta – é considerado supremo. Aqueles que têm fé no Nobre Caminho Óctuplo têm fé naquilo que é supremo; e para aqueles que têm fé no que é supremo, supremo será o resultado. </p>

<p class=Normal> (3) “Entre todos os fenômenos que são condicionados ou não-condicionados, o desapego é declarado como supremo dentre todos, isto é, subjugar a presunção, remover as impurezas, remover a permanência, interromper o ciclo, destruir o desejo, desapego, cessação, nibbana. Aqueles que têm convicção no Dhamma têm convicção no supremo e para aqueles que têm convicção no que é supremo, supremo será o resultado. </p>

<p class=Normal> (4) “Entre todas as comunidades ou grupos que possam haver, a Sangha dos discípulos do Tathagata é considerada suprema – isto é, os quatro [grupos dos nobres discípulos] quando tomados como pares, os oito quando tomados como indivíduos. Aqueles que têm fé na Sangha têm fé naquilo que é supremo; e para aqueles que têm fé no que é supremo, supremo será o resultado. </p>

<p class=Normal> (5) “Entre todos os comportamentos virtuosos, o comportamento virtuoso apreciado pelos nobres é declarado como supremo dentre todos, isto é, quando é intacto, não-lacerado, imaculado, não-matizado, libertador, elogiado pelos sábios, desapegado, que conduz à concentração. Aqueles que têm o comportamento virtuoso apreciado pelos nobres têm o comportamento supremo e para aqueles que cumprem o supremo, o resultado será supremo. </p>

<p class=Normal>Com fé, <br>
compreendendo que o Dhamma supremo<br>
é supremo; <br>
fé no supremo Buda, <br>
insuperável<br>
merecedor de oferendas; <br>
fé no supremo Dhamma, <br>
a calma do desapego, <br>
bem-aventurança; <br>
fé na suprema Sangha, <br>
insuperável<br>
como um campo de mérito; <br>
tendo dado oferendas para o que é supremo, <br>
ele desenvolve mérito supremo, <br>
vida longa e beleza, <br>
fama, honra, <br>
felicidade e poder. </p>

<p class=Normal>Tendo dado para aquilo que é supremo, <br>
a pessoa sábia, centrada<br>
no supremo Dhamma, <br>
quer seja tornando-se um ser divino ou humano, <br>
se alegra, <br>
tendo realizado aquilo que é supremo. </p>

