<p class=Normal style='margin-left:.75in'>
 <p class=Normal><b>Notas:</b></p>

<p class=Normal><a name=N1></a> [1] <i>Viññanatthitiyo</i> é usada com um sentido diferente do <a href="SNXXII.54.php">SN XXII.54</a> no qual os quatro <i>viññanatthitiyo</i> são os quatro agregados que servem de suporte para <i>viññana</i>. Neste sutta <i> viññanatthitiyo</i> se refere a mundos de renascimento. [<a href="#R1">Retorna</a>] </p>

<p class=Normal><a name=N2></a> [2] Os comentários explicam que "os seres humanos são distintos no corpo porque não há duas pessoas cujos corpos sejam exatamente idênticos. Eles são distintos na percepção porque em alguns casos a percepção de renascimento pode ter três raízes, em outras duas, e ainda outros sem nenhuma raiz. Os devas mencionados são aqueles dos seis mundos do reino da esfera sensual. Os seres nos mundos inferiores são certos yakkhas e espíritos fora dos estados de privação." [<a href="#R2">Retorna</a>] </p>

<p class=Normal><a name=N3></a> [3] Os comentários explicam que "esses são os devas do cortejo de Brahma, os ministros de Brahma e os grandes Brahmas. Os seus corpos são distintos em difusão de acordo com o seu respectivo nível, mas a sua percepção é a mesma pois todos têm a percepção relativa ao primeiro jhana. Os seres nos quatro mundos inferiores também fazem parte desse grupo . Os seus corpos são distintos, mas todos possuem uma percepção de renascimento que é resultante insalubre sem raiz." [<a href="#R3">Retorna</a>] </p>

<p class=Normal><a name=N4></a> [4] Os comentários explicam que "os devas que emanam radiância (<i>abhassara deva</i>) representam todas as três classes de devas que fazem parte do segundo jhana: <i>paritthaba deva, appamanabha deva, abhassara deva</i>. Em cada plano os seus corpos são idênticos tendo a mesma difusão, mas a sua percepção é diferente em que alguns não possuem o pensamento aplicado mas retêm o pensamento sustentado, enquanto que outros não possuem o pensamento aplicado e o pensamentos sustentado." [<a href="#R4">Retorna</a>] </p>

<p class=Normal><a name=N5></a> [5] Os comentários explicam que "os devas da glória refulgente (<i>subhakinna deva</i>) são idênticos no corpo e também na percepção porque todos possuem a percepção relativa ao quarto jhana." [<a href="#R5">Retorna</a>] </p>


