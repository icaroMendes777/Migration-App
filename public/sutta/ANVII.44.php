<p class=Normal> <a name=R1></a>"Bhikkhus, há essas sete estações para a consciência. [<a href="#N1">1</a>] Quais sete? </p>

<p class=Normal> (1) "Bhikkhus, há seres que são distintos no corpo e distintos na percepção, como os seres humanos, alguns devas, e alguns seres nos mundos inferiores. Essa é a primeira estação para a consciência. [<a href="#N2">2</a>]</p>

<p class=Normal><a name=R2></a> (2) "Há seres que são distintos no corpo mas idênticos na percepção, como os devas na companhia de Brahma que renascem através do primeiro jhana. Essa é a segunda estação para a consciência. [<a href="#N3">3</a>]</p>

<p class=Normal><a name=R3></a> (3) "Há seres que são idênticos no corpo mas distintos na percepção, como os devas que emanam radiância. Essa é a terceira estação para a consciência. [<a href="#N4">4</a>]</p>

<p class=Normal><a name=R4></a> (4) "Há seres que são idênticos no corpo e idênticos na percepção, como os devas da glória refulgente. Essa é a quarta estação para a consciência. [<a href="#N5">5</a>]</p>

<p class=Normal><a name=R5></a> (5) "Há seres que com a completa superação das percepções da forma, com o desaparecimento das percepções do contato sensorial, sem dar atenção às percepções da diversidade, [percebendo] o ‘espaço é infinito,’ pertencem à base do espaço infinito. Essa é a quinta estação para a consciência. </p>

<p class=Normal> (6) "Há seres que com a completa superação da base do espaço infinito, [percebendo] 'a consciência é infinita,' pertencem à base da consciência infinita. Essa é a sexta estação para a consciência. </p>

<p class=Normal> (7) "Há seres que com a completa superação da base da consciência infinita, [percebendo] ‘não há nada,’ pertencem à base do nada. Essa é a sétima estação para a consciência. </p>

<p class=Normal>"Essas, bhikkhus, são as sete estações para a consciência." </p>


