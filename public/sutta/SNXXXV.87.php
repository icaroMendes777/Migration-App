<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'> Assim
ouvi. Em certa ocasião, o Abençoado estava em Rajagaha, no Bambual, no
Santuário dos Esquilos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'> Agora,
naquela ocasião o venerável Sariputta, o venerável Maha Cunda e o venerável
Channa estavam na montanha do Pico do Abutre. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'> Naquela ocasião,
o venerável Channa estava atormentado, sofrendo e gravemente enfermo. Então, ao
anoitecer, o venerável Sariputta levantou da meditação, foi até o venerável
Maha Cunda e disse: “Amigo Cunda, vamos até o venerável Channa para perguntar
sobre a sua enfermidade.” – “Sim, amigo,” o venerável Maha Cunda respondeu.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'> Então, o
venerável Sariputta e o venerável Maha Cunda foram até o venerável Channa e
eles se cumprimentaram. Quando a conversa amigável e cortês havia terminado,
eles sentaram a um lado e o venerável Sariputta disse para o venerável Channa:
“Eu espero que você esteja melhorando, amigo Channa, espero que você esteja
confortável, espero que as suas sensações de dor estejam diminuindo e não
aumentando e que<span style="mso-spacerun: yes">  </span>a sua diminuição, não
o seu aumento, seja evidente.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'> “Amigo
Sariputta, eu não estou melhorando, não me sinto confortável. Minhas sensações
de dor estão aumentando, não diminuindo ... (igual ao <a href="MN143.php#T4">MN
143.4</a>) ... o seu aumento, não a sua diminuição é evidente. Eu usarei a
faca; [<a href="#N1">1</a>] <a name=R1></a>amigo Sariputta; eu não tenho desejo
de viver.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'> “Que o
venerável Channa não use a faca. Que o venerável Channa viva. Nós queremos que
o Venerável Channa viva. Se lhe falta comida adequada, eu irei em busca de
comida adequada. Se lhe faltam medicamentos adequados, eu irei em busca de
medicamentos adequados. Se lhe falta um assistente, eu o assistirei. Que o
venerável Channa não use a faca. Que o venerável Channa viva. Nós queremos que
o Venerável Channa viva.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'> “Amigo
Sariputta, não é que eu não tenha comida e medicamentos adequados ou
assistência adequada. Mas, amigo Sariputta, o Mestre há muito tempo tem sido
venerado por mim com amor, não sem amor; pois é apropriado que um discípulo
venere o Mestre com amor, não sem amor. Amigo Sariputta, lembre-se disto: o
bhikkhu Channa usará a faca de modo puro.” [<a href="#N2">2</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'> “Nós gostaríamos de fazer algumas perguntas ao venerável Channa, se
esta for uma ocasião oportuna.” – “Pergunte, amigo Sariputta, ao ouvir, eu
saberei.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'> “Amigo
Channa, você considera o olho, a consciência no olho e as coisas conscientizadas pela consciência no olho assim: ‘Isso é meu, isso sou eu,
isso é o meu eu’? Você considera o ouvido ... o nariz ... a língua ... o corpo
... a mente, a consciência na mente e as coisas conscientizadas pela consciência na mente assim: ‘Isso é meu, isso sou eu, isso é o meu eu’?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigo
Sariputta, eu considero o olho, a consciência no olho e as coisas conscientizadas pela consciência no olho assim: ‘Isso não é meu, isso não
sou eu, isso não é o meu eu.’ Eu considero o ouvido ... o nariz ... a língua
... o corpo ... a mente, a consciência na mente e as coisas conscientizadas pela consciência na mente assim: ‘Isso não é meu, isso não sou eu,
isso não é o meu eu.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'> “Amigo
Channa, o que você viu, o que você compreendeu diretamente no olho, na
consciência no olho e nas coisas conscientizadas pela da consciência
no olho, para<span style="mso-spacerun: yes">  </span>considerá-los assim:
‘Isso não é meu, isso não sou eu, isso não é o meu eu’? O que você viu, o que
você compreendeu diretamente no ouvido ... no nariz ... na língua ... no corpo
... na mente, na consciência na mente e nas coisas conscientizadas pela consciência na mente, para<span style="mso-spacerun: yes"> 
</span>considerá-los assim: ‘Isso não é meu, isso não sou eu, isso não é o meu
eu’?”<o:p></o:p></span></p>



<p class=Normal><a name="T11"></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigo
Sariputta, é vendo a cessação, através do conhecimento direto da cessação no
olho, na consciência no olho e nas coisas conscientizadas pela
consciência no olho, que eu os considero assim: ‘Isso não é meu, isso não sou
eu, isso não é o meu eu.’ É vendo a cessação, através do conhecimento direto da
cessação no ouvido ... no nariz ... na língua ... no corpo ... na mente, na
consciência na mente e nas coisas conscientizadas pela
consciência na mente, que eu os considero assim: ‘Isso não é meu, isso não sou
eu, isso não é o meu eu.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'> Quando
isso foi dito, o venerável Maha Cunda disse para o venerável Channa: [<a
href="#N3">3</a>] <a name=R3></a>“Conseqüentemente, amigo Channa, esta
instrução do Abençoado deve receber atenção constante: ‘Há vacilação naquele
que é dependente, não há vacilação naquele que é independente; onde não há
vacilação, há tranqüilidade; onde há tranqüilidade, não há preferências; onde
não há preferências, não há ir e vir; quando não há ir e vir, não há
falecimento e renascimento; quando não há falecimento e renascimento, não
há o aqui nem o além, nem o que está entre os dois. Esse é o fim do sofrimento.’”
[<a href="#N4">4</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'> Então, o venerável Sariputta e o venerável Maha Cunda depois de
assim aconselharem o venerável Channa, levantaram dos seus assentos e partiram.
E pouco depois de haverem partido, o venerável Channa usou a faca. [<a
href="#N5">5</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R5></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'> Então, o venerável Sariputta foi até o Abençoado e depois de
cumprimentá-lo, sentou a um lado e disse: “Venerável senhor, o venerável Channa
usou a faca. Qual será o seu destino, qual será o seu futuro percurso?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sariputta,
o bhikkhu Channa não declarou para você a pureza dele?” [<a href="#N6">6</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Venerável senhor, há um vilarejo Vajjia chamado Pubbajira. Lá o
venerável Channa tinha famílias que eram amigas dele, famílias que eram íntimas
dele, famílias que mereceriam censura.” [<a href="#N7">7</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R7></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Há essas famílias que eram amigas do bhikkhu Channa, Sariputta,
famílias que eram íntimas dele, famílias que mereceriam censura; mas eu não
digo que por isso ele seria censurável. Sariputta, quando alguém deita este
corpo e se apega a um novo corpo, então eu digo que esse alguém é censurável.
Não houve nada disso no bhikkhu Channa; o bhikkhu Channa usou a faca de modo
puro.” [<a href="#N8">8</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R8></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Isso foi o que disse o Abençoado. O venerável Sariputta ficou satisfeito
e contente com as palavras do Abençoado.<o:p></o:p></span></p>


