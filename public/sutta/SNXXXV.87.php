<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[1] Esta é uma expressão elíptica para o
suicídio. [<a href="#R1">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[2] Fazendo esta afirmação ele está de modo
implícito reivindicando o estado de arahant, tal como ficará claro no verso 13.
Se a sua afirmação em relação a isso era válida ou não, é incerto, o comentário
a considera um caso de avaliação exagerada. [<a href="#R2">Retorna</a>] <o:p></o:p></span></p>


<p class=Normal><a name=N3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[3] MA diz que o Ven. Maha Cunda deu esta
instrução pensando que ele ainda era uma pessoa comum, visto que ele não era
capaz de suportar a dor e desejava se suicidar. [<a href="#R3">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[4] O sentido desta instrução pode ser
explicado com a ajuda do MA assim: alguém é dependente devido ao desejo e
idéias e se torna independente ao abandoná-las com a realização do estado de
arahant. Preferências (<i>nati</i>, literalmente inclinar) surgem através do
desejo, e a sua ausência significa que não há inclinação ou desejo pela
existência. Não há ir e vir através do fim do renascimento e morte; nem aqui,
nem além, nem no que está entre os dois, pela transcendência deste mundo, do mundo
além e da passagem entre um e outro. Esse é o fim do sofrimento das
contaminações e o fim do sofrimento do ciclo de<span style="mso-spacerun:
yes">  </span>renascimentos. [<a href="#R4">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N5></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[5] MA: Ele cortou a garganta e naquele exato
momento o medo da morte surgiu nele e o sinal do futuro renascimento apareceu.
Reconhecendo que ele ainda era uma pessoa comum, ele foi assim estimulado e
desenvolveu o insight compreendendo as formações, alcançando o estado de
arahant exatamente antes de falecer. [<a href="#R5">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[6] MA: Embora o Ven. Channa ainda fosse uma
pessoa comum no momento em que ele fez a declaração, como ele realizou o
parinibbana logo em seguida, o Buda assim falou referindo-se à mesma
declaração. [<a href="#R6">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N7></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[7] <i>Upavajjakulani</i>. De
acordo com MA e MT parece que Channa havia estabelecido um relacionamento
próximo com os seus patrocinadores laicos de um jeito não apropriado para um
monge, e muito embora ele estivesse empenhado na prática correta, essa
intimidade levantou suspeitas dentro da Sangha. [<a href="#R7">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N8></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[8] Esta afirmação parece indicar que Channa
era um arahant no momento em que cometeu o suicídio, embora o comentário dê
outra explicação. [<a href="#R8">Retorna</a>]<b><o:p></o:p></b></span></p>

