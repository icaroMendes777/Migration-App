<p class=Normal>Então o Ven. Anuruddha foi até o Ven. Sariputta e ao chegar
ambos se cumprimentaram. Depois que a conversa cortês e amigável havia
terminado ele sentou a um lado e disse para o Ven. Sariputta: Através do olho
divino, que é purificado e ultrapassa o humano, eu vejo milhares de universos.
Minha energia está estimulada e incansável. Minha atenção plena está
estabelecida e inabalável. Meu corpo está calmo e sem agitação. Minha mente
está concentrada e unificada. No entanto a minha mente não está liberta das
impurezas através do desapego. </p>

<p class=Normal><i>Sariputta</i>: Meu amigo, quando o pensamento lhe ocorre, ‘Através do
olho divino, que é purificado e ultrapassa o humano, eu vejo milhares de universos,’
isso está relacionado com a sua presunção. Quando o pensamento lhe ocorre,
‘Minha energia está estimulada e incansável. Minha atenção plena está
estabelecida e inabalável. Meu corpo está calmo e sem agitação. Minha mente
está concentrada e unificada,’ isso está relacionado com a sua inquietação.
Quando o pensamento lhe ocorre, ‘No entanto a minha mente não está liberta das
impurezas através do desapego,’ isso está relacionado com a sua ansiedade.
Seria bom se - abandonando essas três qualidades, não dando atenção a essas
três qualidades - você dirigisse a sua mente para a propriedade do Imortal.’ </p>

<p class=Normal>Assim, depois disso, o Ven. Anuruddha - abandonando essas três qualidades,
não dando atenção a essas três qualidades – dirigiu a mente dele para a
propriedade do Imortal. Permanecendo só, isolado, diligente, ardente e
decidido, em pouco tempo, ele alcançou e permaneceu no objetivo supremo da vida
santa pelo qual membros de um clã deixam a vida em família pela vida santa,
tendo conhecido e realizado por si mesmo no aqui e agora. Ele soube: “O
nascimento foi destruído, a vida santa foi vivida, o que deveria ser feito foi
feito, não há mais vir a ser a nenhum estado.” E assim o Ven. Anuruddha
tornou-se mais um dos Arahants. </p>

