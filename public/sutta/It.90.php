<p class=Normal><b>Notas:</b></p>

<p class=Normal><a name=N1></a> [1] Possibilitando que alguém siga o nobre caminho e assim alcance a libertação. [<a href="#R1">Retorna</a>]

<p class=Normal><a name=N2></a> [2] <i>Alayasamugghato</i>: <i>alaya</i> pode ser interpretado como: moradia, residência, permanência, lugar para pousar (como no caso de aves), ou também pode ser interpretado como: sede, desejo, apego. <i>Samugghata</i> significa remoção, erradicação.</p>

<p class=Normal> No <a href="SNLVI.11.php"><i>Dhammacakkapavattana Sutta</i></a> a nobre verdade da cessação do sofrimento é descrita como <i>caga, patinissagga, mutti, analaya</i>, em que <i>analaya</i> foi interpretado por Ajaan Brahm como "nada gruda ou se estabelece na mente".</p> 
<p class=Normal>Na tradução para o inglês a interpretação para <i>alayasamugghato</i> foi "remoção da sede". [<a href="#R2">Retorna</a>]</p>

