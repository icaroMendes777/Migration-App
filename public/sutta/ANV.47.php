<p class=Normal>"Bhikkhus, há esses cinco tesouros. Quais cinco? O tesouro da
convicção, o tesouro da virtude, o tesouro do estudo, o tesouro da generosidade, o tesouro da sabedoria. </p>

<p class=Normal>"E o que é o tesouro da convicção? É o caso em que um nobre
discípulo tem convicção, está convencido da iluminação do Tathagata: 'De fato,
esse Abençoado é um arahant,
perfeitamente iluminado, consumado no verdadeiro conhecimento e conduta,
bem-aventurado, conhecedor dos mundos, um líder insuperável de pessoas
preparadas para serem treinadas, mestre de devas e humanos, desperto, sublime.' A isto se
chama o tesouro da convicção. </p>

<p class=Normal>"E o que é o tesouro da virtude? É o caso em que um nobre
discípulo se abstém de matar, se abstém de roubar, se abstém da conduta sexual
imprópria, se abstém de mentir, se abstém de tomar vinho, álcool e outros embriagantes
que causam negligência. A isto se chama o tesouro da virtude. </p>

<p class=Normal>"E o que é o tesouro do estudo? É o caso em que um nobre
discípulo ouviu muito, reteve o que ouviu, memorizou o que ouviu. Todo
ensinamento que é admirável no início, admirável no meio, admirável no final,
com o correto significado e fraseado e que revela uma vida santa que é
completamente perfeita e imaculada: esses ensinamentos ele ouviu com
freqüência, reteve, discutiu, memorizou, examinou com a sua mente e penetrou
corretamente com o entendimento. A isto se chama o tesouro do estudo.</p>

<p class=Normal>
&quot;E qual é o tesouro da generosidade? É o caso em que um nobre
discípulo permanece em casa com uma mente desprovida da mácula da avareza,
espontaneamente generoso, mão aberta, que se delicia com a renúncia, devotado à
caridade, deliciando-se em dar e compartir. A isto se denomina o tesouro da
generosidade.&quot;<o:p></o:p></span></p>


<p class=Normal>
&quot;E qual é o tesouro da sabedoria? É o caso em que um nobre
discípulo tem sabedoria, dotado com a completa compreensão da origem e cessação - nobre,
penetrante, que conduz ao fim do sofrimento. A isto se denomina o tesouro da
sabedoria.&quot; <o:p></o:p></span></p>

<p class=Normal>"Esses bhikkhus, são os cinco tesouros." </p>


