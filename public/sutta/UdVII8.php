<p class=Normal>O Abençoado viu o venerável Mahakaccana sentado com as pernas cruzadas, não muito distante, com o corpo ereto, tendo bem estabelecido a atenção plena no corpo.</p>

<p class=Normal>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p>

<p class=Normal style='margin-left:.75in'> Naquele em quem a atenção plena no corpo<br>
