<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em certa
ocasião, o Abençoado estava<span style="mso-spacerun: yes">  </span>em Vesali
na Grande Floresta no Salão com um pico na cumeeira. Então, ao amanhecer, o venerável
Ananda se vestiu e tomando a tigela e o manto externo, foi para Vesali para
esmolar alimentos. O Venerável Ananda viu um grande número de jovens Licchavi
praticando o tiro com arco no salão de treinamento, atirando flechas a uma
certa distância através de um pequeno orifício, com a ponta através da parte
traseira, [<a href="#N1">1</a>] <a name=R1></a>sem errar. Ao ver isso, lhe
ocorreu o seguinte pensamento: “Esse jovens Licchavi de fato são bem treinados!
Esse jovens Licchavi de fato são bem treinados, atirando flechas a uma certa
distância através de um pequeno orifício, com a ponta através da parte
traseira, sem errar.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então,
depois do venerável Ananda haver esmolado em Vesali e de haver retornado, após
a refeição ele foi até o Abençoado e depois de cumprimentá-lo sentou a um lado,
e relatou aquilo que ele havia visto.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>[O
Abençoado disse:] “O que você pensa, Ananda, o que é mais difícil e desafiador:
atirar flechas a uma certa distância através de um pequeno orifício, com a
ponta através da parte traseira, sem errar, ou perfurar com a ponta da flecha a
ponta de um cabelo partido em sete fios?”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“É mais
difícil e desafiador, venerável senhor, perfurar com a ponta da flecha a ponta
de um cabelo partido em sete fios.”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas,
Ananda, eles perfuram algo ainda mais difícil de perfurar aqueles que perfuram
como na verdade é : ‘Isto é sofrimento’; aqueles que perfuram como na verdade
é: ‘Esta é a origem do sofrimento’; aqueles que perfuram como na verdade é:
‘Esta é a cessação do sofrimento’;<span style="mso-spacerun: yes"> 
</span>aqueles que perfuram como na verdade é : ‘Este é o caminho que conduz à
cessação do sofrimento.’<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
Ananda, um esforço deve ser feito para compreender: ‘Isto é sofrimento.’ Um
esforço deve ser feito para compreender: ‘Esta é a origem do sofrimento.’ Um
esforço deve ser feito para compreender: ‘Esta é a cessação do sofrimento.’ Um
esforço deve ser feito para compreender: ‘Este é o caminho que conduz à
cessação do sofrimento.’”<o:p></o:p></span></p>

