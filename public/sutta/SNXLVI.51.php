<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, eu ensinarei para vocês o alimento e a esfomeação em
relação aos cinco obstáculos e aos sete fatores da iluminação. Ouçam e prestem
muita atenção àquilo que eu vou dizer.” – “Sim, venerável senhor,” os bhikkhus
responderam. O Abençoado disse o seguinte: <o:p></o:p></span></p>



<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>(Alimentando
os Obstáculos) </span></b><span lang=PT-BR style='mso-ansi-language:PT-BR'>[<a
href="#N1">1</a>]<o:p></o:p></span></p>


<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“E qual, bhikkhus, é o alimento para o surgimento do <i>desejo sensual </i>que
ainda não surgiu ou para o crescimento e incremento do desejo sensual, uma vez
que este tenha surgido? Há, bhikkhus, o sinal da beleza: dar com freqüência
atenção sem sabedoria<span style='mso-spacerun:yes'>  </span>para isso, esse é
o alimento para o surgimento do desejo sensual<i> </i>que ainda não surgiu ou
para o crescimento e incremento do desejo sensual, uma vez que este tenha
surgido.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é o alimento para o surgimento da <i>má vontade </i>que ainda não
surgiu ou para o crescimento e incremento da má vontade, uma vez que esta tenha
surgido? Há, bhikkhus, o sinal do repulsivo: dar com freqüência atenção sem
sabedoria<span style='mso-spacerun:yes'>  </span>para isso, esse é o alimento
para o surgimento da má vontade<i> </i>que ainda não surgiu ou para o
crescimento e incremento da má vontade, uma vez que esta tenha surgido.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é o alimento para o surgimento da <i>preguiça e torpor </i>que ainda
não surgiram ou para o crescimento e incremento da preguiça e torpor uma vez
que estes tenham surgido? Há, bhikkhus, a letargia descontente, o
espreguiçamento preguiçoso, a sonolência após a refeição e a preguiça mental:
dar com freqüência atenção sem sabedoria<span style='mso-spacerun:yes'> 
</span>para isso, esse é o alimento para o surgimento da preguiça e torpor que
ainda não surgiram ou para o crescimento e incremento da preguiça e torpor, uma
vez que estes tenham surgido.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é o alimento para o surgimento da <i>inquietação e ansiedade </i>que
ainda não surgiram ou para o crescimento e incremento da inquietação e ansiedade
uma vez que estas tenham surgido? Há, bhikkhus, a mente conturbada: dar com
freqüência atenção sem sabedoria<span style='mso-spacerun:yes'>  </span>para
isso, esse é o alimento para o surgimento da inquietação e ansiedade que ainda
não surgiram ou para o crescimento e incremento da inquietação e ansiedade, uma
vez que estas tenham surgido.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é o alimento para o surgimento da <i>dúvida </i>que ainda não surgiu
ou para o crescimento e incremento da dúvida uma vez que esta tenha surgido? Há,
bhikkhus, coisas que são a base da dúvida: dar com freqüência atenção sem
sabedoria<span style='mso-spacerun:yes'>  </span>para isso, esse é o alimento
para o surgimento da dúvida que ainda não surgiu ou para o crescimento e
incremento da dúvida, uma vez que esta tenha surgido.<o:p></o:p></span></p>



<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>(Alimentando
os Fatores da Iluminação)<o:p></o:p></span></b></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é o alimento para o surgimento da <i>atenção plena </i>como um fator
da iluminação que ainda não surgiu ou para o crescimento e incremento da
atenção plena como fator da iluminação, uma vez que esta tenha surgido? Há,
bhikkhus, coisas que são a base da atenção plena como fator da iluminação: dar
com freqüência atenção com sabedoria<span style='mso-spacerun:yes'> 
</span>para isso, esse é o alimento para o surgimento da atenção plena como
fator da iluminação<i> </i>que ainda não surgiu ou para o crescimento e
incremento da atenção plena como um fator da iluminação, uma vez que esta tenha
surgido.[<a href="#N2">2</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“E qual, bhikkhus, é o alimento para o surgimento da <i>investigação dos
fenômenos </i>como um fator da iluminação que ainda não surgiu ou para o
crescimento e incremento da investigação dos fenômenos como fator da
iluminação, uma vez que esta tenha surgido? Há, bhikkhus, qualidades mentais
que são hábeis e inábeis, benéficas e prejudiciais, superiores e inferiores,
claras e escuras com as suas contrapartidas: dar com freqüência atenção com
sabedoria<span style='mso-spacerun:yes'>  </span>para isso, esse é o alimento
