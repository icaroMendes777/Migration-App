PT-BR'>[4] Outras onze condições são: (i) refletir sobre o horror dos planos de
miséria; (ii) ver os benefícios do estímulo da energia; (iii) refletir que
estamos seguindo o mesmo caminho trilhado por todos os Budas, etc.; (iv)
refletir sobre a necessidade de honrar todas as oferendas recebidas; (v-viii)
refletir sobre a grandeza da tradição, do Mestre, da linhagem e dos bhikkhus
companheiros; (ix-xi) evitar pessoas preguiçosas, associar-se com pessoas
energéticas e determinação correta. [<a href="#R4">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N5></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[5] Outras onze condições são: (i-vii) recordar-se do Buda, Dhamma,
Sangha, da própria virtude, da própria generosidade, dos devas e dos estados
pacíficos; (viii) evitar pessoas grosseiras; (ix) associar-se com pessoas
gentis; (x) refletir acerca de suttas inspiradores; (xi) determinação correta.
[<a href="#R5">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[6] Outras sete condições são: (i) alimentação nutritiva; (ii) clima
favorável; (iii) postura correta; (iv) comportamento equilibrado; (v-vii)
evitar pessoas nervosas, associar-se com pessoas calmas e determinação correta.
[<a href="#R6">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N7></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[7] Outras dez condições são: (i) asseio pessoal; (ii) equilíbrio das cinco
faculdades; (iii) habilidade com o sinal (objeto da meditação); (iv-vi) estimular,
conter e alegrar a mente, cada um no seu momento adequado; (vii) observar com
equanimidade no momento adequado; (viii-x) evitar pessoas desconcentradas,
associar-se com pessoas concentradas e determinação correta. O comentário ao <a
href="MN10.php">Satipatthana Sutta</a> adiciona, como décimo primeiro fator,
refletir sobre os jhanas e as libertações. [<a href="#R7">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N8></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[8] Outras cinco condições são: (i) uma atitude desapegada em relação
aos outros seres; (ii) uma atitude desapegada em relação às formações, (objetos
inanimados); (iii-v) evitar pessoas possessivas, associar-se com pessoas
equânimes e determinação correta. [<a href="#R8">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N9></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[9] O sinal da repulsa ou a contemplação das impurezas compreende a
contemplação das partes do corpo e as contemplações do cemitério descritas no <a
href="MN10.php">Satipatthana Sutta</a>. [<a href="#R9">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N10></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[10] A libertação da mente através do amor bondade, <i>(mettacetovimutti),
</i>corresponde aos jhanas. Seis coisas conduzem ao abandono da má vontade: (i)
familiarizar-se com o objeto do amor bondade; (ii) devoção à meditação de amor
bondade; (iii) refletir sobre a nossa responsabilidade para com as próprias
ações; (iv) considerar com freqüência; (v) pessoas admiráveis como bons amigos;
(vi) linguagem apropriada. A má vontade é completamente abandonada pelo caminho
do que não retorna. [<a href="#R10">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N11></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[11] Com relação aos três elementos da energia veja o <a
href="SNXLVI.2.php#N6">SN XLVI.2 – nota 6</a>. Seis coisas conduzem ao abandono
da preguiça e torpor: (i) evitar comer em excesso; (ii) mudar de postura; (iii)
dar atenção à percepção da luz; (iv) permanecer ao ar livre; (v) pessoas
admiráveis como bons amigos; (v) linguagem apropriada. A preguiça e o torpor
são completamente abandonados com o caminho do arahant. [<a href="#R11">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N12></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[12] Seis coisas conduzem ao abandono da inquietação e ansiedade: (i)
muito estudo; (ii) investigação; (iii) familiaridade com o Vinaya;
(iv) associar-se com pessoas maduras; (v) pessoas admiráveis como bons amigos;
(vi) linguagem apropriada. A inquietação é abandonada pelo caminho do arahant e
a ansiedade pelo caminho do que não retorna. [<a href="#R12">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N13></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[13] Seis coisas conduzem ao abandono da dúvida: (i) muito estudo; (ii)
investigação; (iii) familiaridade com o Vinaya; (iv) firmeza; (v) pessoas
admiráveis como bons amigos; (vi) linguagem apropriada. A dúvida é abandonada
pelo caminho daquele que entra na correnteza. [<a href="#R13">Retorna</a>]<o:p></o:p></span></p>





