ainda não surgiu ou para o crescimento e incremento da investigação dos
fenômenos como<span style='mso-spacerun:yes'>  </span>fator da iluminação, uma
vez que esta tenha surgido.[<a href="#N3">3</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R3></a><span lang=PT-BR style='mso-ansi-language:
