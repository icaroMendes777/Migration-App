PT-BR;mso-bidi-font-weight:bold'>[8]</span><b style='mso-bidi-font-weight:normal'><span
lang=PT-BR style='mso-ansi-language:PT-BR'> </span></b><span lang=PT-BR
style='mso-ansi-language:PT-BR;mso-bidi-font-weight:bold'>MA diz que embora a
