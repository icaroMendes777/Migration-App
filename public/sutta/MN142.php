<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião o Abençoado estava entre os Sakyas em Kapilavatthu, no
Parque de Nigrodha.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. Então
Mahapajapati Gotami tomou uma peça de tecido e foi até o Abençoado.[<a
href="#N1">1</a>] <a name=R1></a>Depois de cumprimentá-lo, ela sentou a um lado
e disse para o Abençoado: “Venerável senhor, esta peça de tecido foi tecida por
mim, esta peça de tecido foi urdida por mim, especialmente para o Abençoado.
Venerável senhor, que o Abençoado aceite isso de mim por compaixão.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isso
foi dito, o Abençoado respondeu: “Dê para a Sangha, Gotami. Ao doar para a
Sangha, a oferenda será feita tanto para mim como para a Sangha.” [<a href="#N2">2</a>]
<a name=R2></a>Uma segunda vez e uma terceira vez ela disse para o Abençoado:<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Venerável
senhor,... aceite isso de mim por compaixão.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Uma segunda
vez e uma terceira vez o Abençoado respondeu: “Dê para a Sangha, Gotami. Ao
fazer para a Sangha, a oferenda será feita tanto para mim como para a Sangha.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. Então o
venerável Ananda disse para o Abençoado: “Venerável senhor, que o Abençoado
aceite essa peça de tecido nova de Mahapajapati Gotami. Mahapajapati Gotami foi
de grande ajuda para o Abençoado, venerável senhor. Sendo a irmã da sua mãe,
ela foi a sua babá, sua mãe adotiva, sua ama-de-leite. Ela amamentou o
Abençoado quando a sua mãe morreu. O Abençoado também tem sido de grande ajuda
para Mahapajapati Gotami, venerável senhor. É por causa do Abençoado que
Mahapajapati Gotami buscou refúgio no Buda, no Dhamma e na Sangha. É por causa
do Abençoado que Mahapajapati Gotami se abstém de matar seres vivos, de tomar aquilo
que não é dado, da conduta imprópria em relação aos prazeres sensuais, da
linguagem mentirosa e se abstém do vinho, álcool e outros embriagantes, que
causam a negligência. É por causa do Abençoado que Mahapajapati Gotami possui
perfeita claridade, serenidade e confiança no Buda, no Dhamma e na Sangha e possui a virtude apreciada
pelos nobres. [<a href="#N3">3</a>] <a name=R3></a>É por causa do Abençoado que
Mahapajapati Gotami está livre da dúvida com relação ao sofrimento, com relação
à origem do sofrimento, com relação à cessação do sofrimento e com relação ao
caminho que conduz à cessação do sofrimento. O Abençoado tem sido de grande
ajuda para Mahapajapati Gotami.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. “Assim
é, Ananda, assim é! Quando uma pessoa, por causa de outrem, busca refúgio no
Buda, no Dhamma e na Sangha, eu digo que não é fácil que a primeira repague a
esta última homenageando-a, servindo-a, fazendo saudações com reverência e
prestando serviços com cortesia, proporcionando mantos, comida esmolada,
moradia e medicamentos. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando uma
pessoa, por causa de outrem, se abstém de matar seres vivos, de tomar aquilo
que não é dado, da conduta imprópria em relação aos prazeres sensuais, da
linguagem mentirosa, e se abstém do vinho, álcool e outros embriagantes, que
causam a negligência, eu digo que não é fácil que a primeira repague a esta
última homenageando-a ... medicamentos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando uma
pessoa, por causa de outrem, possui perfeita claridade, serenidade e confiança no Buda, no Dhamma e na
Sangha e possui a virtude apreciada pelos nobres, eu digo que não é fácil que a
primeira repague a esta última homenageando-a ... medicamentos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando uma
pessoa, por causa de outrem, está livre da dúvida com relação ao sofrimento,
com relação à origem do sofrimento, com relação à cessação do sofrimento e com
relação ao caminho que conduz à cessação do sofrimento, eu digo que não é fácil
que a primeira repague a esta última homenageando-a ... medicamentos.<o:p></o:p></span></p>



<p class=Normal><a name=T5></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>5. “Há catorze tipos de oferendas pessoais, Ananda. [<a href="#N4">4</a>]
<a name=R4></a>Alguém faz uma oferenda para o Tathagata, um arahant, perfeitamente
iluminado; esse é o primeiro tipo de oferenda pessoal. Alguém faz uma oferenda
para um paccekabuddha; esse é o segundo tipo de oferenda pessoal. Alguém faz
uma oferenda para um arahant discípulo do Tathagata; esse é o terceiro tipo de
oferenda pessoal. Alguém faz uma oferenda para aquele que entrou no caminho
para a realização do fruto de arahant; esse é o quarto tipo de oferenda
pessoal. Alguém faz uma oferenda para aquele que não retorna; esse é o quinto
tipo de oferenda pessoal. Alguém faz uma oferenda para aquele que entrou no
caminho para a realização do fruto daquele que não retorna; esse é o sexto tipo
de oferenda pessoal. Alguém faz uma oferenda para aquele que retorna apenas uma
vez; esse é o sétimo tipo de oferenda pessoal. Alguém faz uma oferenda para
aquele que entrou no caminho para a realização do fruto daquele que retorna
apenas uma vez; esse é o oitavo tipo de oferenda pessoal. Alguém faz uma
oferenda para aquele que entrou na correnteza; esse é o nono tipo de oferenda
pessoal. Alguém faz uma oferenda para aquele que entrou no caminho para a
realização do fruto daquele que entrou na correnteza; [<a href="#N5">5</a>]<a
name=R5></a> esse é o décimo tipo de oferenda pessoal. Alguém faz uma oferenda
para alguém de fora [do Dhamma e Disciplina] que está livre da cobiça pelos
prazeres sensuais; [<a href="#N6">6</a>] <a name=R6></a>esse é o décimo
primeiro tipo de oferenda pessoal. Alguém faz uma oferenda para uma pessoa
comum virtuosa; esse é o décimo segundo tipo de oferenda pessoal. Alguém faz uma
oferenda para uma pessoa comum não virtuosa;<span style='mso-spacerun:yes'> 
</span>esse é o décimo terceiro tipo de oferenda pessoal. Alguém faz uma
oferenda para um animal; esse é o décimo quarto tipo de oferenda pessoal.<o:p></o:p></span></p>



<p class=Normal><a name=T6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>6. “Neste caso, Ananda, ao fazer uma oferenda para um animal, pode-se
esperar que a oferenda seja recompensada cem vezes. [<a href="#N7">7</a>] <a
name=R7></a>Ao fazer uma oferenda para uma pessoa comum não virtuosa, pode-se
esperar que a oferenda seja recompensada mil vezes. Ao fazer uma oferenda para uma
pessoa comum virtuosa, pode-se esperar que a oferenda seja recompensada cem mil
vezes. Ao fazer uma oferenda para alguém de fora [do Dhamma e Disciplina] que
está livre da cobiça pelos prazeres sensuais, pode-se esperar que a oferenda
seja recompensada cem mil, cem mil vezes.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ao fazer
uma oferenda para aquele que entrou no caminho para a realização do fruto
daquele que entrou na correnteza, pode-se esperar que a oferenda seja
recompensada de modo incalculável, imensurável. O que, então, deve ser dito de
uma oferenda para aquele que entrou na correnteza? O que deve ser dito de uma
oferenda para aquele que entrou no caminho para a realização do fruto daquele
que retorna uma vez ...<span style='mso-spacerun:yes'>  </span>para aquele que
retorna uma vez ... para aquele que entrou no caminho para a realização do
fruto daquele que não retorna ... para aquele que não retorna ... para aquele
que entrou no caminho para a realização do fruto de arahant ... um arahant ...
um<span style='mso-spacerun:yes'>  </span>paccekabuddha? O que deve ser dito de
uma oferenda para um Tathagata, um arahant, perfeitamente iluminado?[<a href="#N8">8</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R8></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>7. “Há sete tipos de oferendas para a Sangha, Ananda. Alguém faz uma
oferenda para ambas as Sanghas, [bhikkhus e bhikkhunis], lideradas pelo Buda;
esse é o primeiro tipo de oferenda<span style='mso-spacerun:yes'>  </span>para
a Sangha.[<a href="#N9">9</a>] <a name=R9></a>Alguém faz uma oferenda para
ambas as Sanghas. [bhikkhus e bhikkhunis], depois que o Tathagata realizou o
parinibbana; esse é o segundo tipo de oferenda<span
style='mso-spacerun:yes'>  </span>para a Sangha. Alguém faz uma oferenda para a
Sangha dos bhikkhus; esse é o terceiro tipo de oferenda<span
style='mso-spacerun:yes'>  </span>para a Sangha. Alguém faz uma oferenda para a
Sangha das bhikkhunis; esse é o quarto tipo de oferenda<span
style='mso-spacerun:yes'>  </span>para a Sangha. Alguém faz uma oferenda,
dizendo: ‘Nomeie tantos bhikkhus e bhikkhunis em meu nome para a Sangha’; esse
é o quinto tipo de oferenda<span style='mso-spacerun:yes'>  </span>para a
Sangha. Alguém faz uma oferenda, dizendo: ‘Nomeie tantos bhikkhus em meu nome
para a Sangha’; esse é o sexto tipo de oferenda<span style='mso-spacerun:yes'> 
</span>para a Sangha. Alguém faz uma oferenda, dizendo: ‘Nomeie tantas
bhikkhunis em meu nome para a Sangha’; esse é o sétimo tipo de oferenda<span
style='mso-spacerun:yes'>  </span>para a Sangha.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. “No
futuro, Ananda, haverá membros do clã com o ‘pescoço amarelo,’ não virtuosos,
com mau caráter. [<a href="#N10">10</a>] <a name=R10></a>As pessoas farão
oferendas para esses indivíduos não virtuosos em benefício da Sangha. Mesmo
nesse caso, eu digo, uma oferenda feita para a Sangha é incalculável,
imensurável. [<a href="#N11">11</a>] <a name=R11></a>E digo que de nenhuma
forma uma oferenda feita<span style='mso-spacerun:yes'>  </span>para alguém
individualmente poderá trazer maior recompensa do que uma oferenda feita para a
Sangha. [<a href="#N12">12</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R12></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>9. “Há quatro tipos de purificação das oferendas. Quais quatro? Há a
oferenda que é purificada pelo doador, mas não pelo receptor. [<a href="#N13">13</a>]
<a name=R13></a>Há a oferenda que é purificada pelo receptor, mas não pelo
doador. Há a oferenda que não é purificada nem pelo doador e tampouco pelo
receptor. Há a oferenda que é purificada por ambos, doador e<span
style='mso-spacerun:yes'>  </span>receptor.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. “E qual
é a<span style='mso-spacerun:yes'>  </span>oferenda purificada pelo doador, mas
não pelo receptor? Aqui o doador é virtuoso, de bom caráter, e o receptor é não
virtuoso, de mau caráter. Assim a oferenda é purificada pelo doador, não pelo
receptor.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. “E qual
é a<span style='mso-spacerun:yes'>  </span>oferenda purificada pelo receptor,
mas não pelo doador? Aqui o doador é não virtuoso, de mau caráter, e o receptor
é virtuoso, de bom caráter. Assim a oferenda é purificada pelo receptor, não
pelo doador.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. “E qual
é a<span style='mso-spacerun:yes'>  </span>oferenda que não é purificada nem
pelo doador e tampouco pelo receptor? Aqui o doador é não virtuoso, de mau
caráter, e o receptor é não virtuoso, de mau caráter. Assim a oferenda não é
purificada nem pelo doador e tampouco pelo receptor.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. “E qual
é a<span style='mso-spacerun:yes'>  </span>oferenda purificada por ambos,
doador e receptor? Aqui o doador é virtuoso, de bom caráter, e o receptor é
virtuoso, de bom caráter. Assim a oferenda é purificada por ambos, doador e
receptor. Esses são os quatro tipos de purificação das oferendas.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14. Isso
foi o que disse O Abençoado. Tendo dito isso, o Mestre disse mais:<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Quando uma pessoa virtuosa faz para uma pessoa não virtuosa <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>com o coração confiante uma oferenda obtida corretamente,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>depositando fé em que o fruto da ação será imenso,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>a virtude do doador purifica a oferenda.</p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Quando uma pessoa não virtuosa faz para uma pessoa virtuosa<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>com o coração desconfiado uma oferenda obtida de modo incorreto,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>e sequer deposita fé em que o fruto da ação será imenso,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>a virtude do recebedor purifica a oferenda.</p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Quando uma pessoa não virtuosa faz para uma pessoa não virtuosa <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>com o coração desconfiado uma oferenda obtida de modo incorreto,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>e sequer deposita fé em que o fruto da ação será imenso,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>a virtude de nenhum purifica a oferenda.</p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Quando uma pessoa virtuosa faz para uma pessoa virtuosa<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>com o coração confiante uma oferenda obtida corretamente,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>depositando fé em que o fruto da ação será imenso,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>essa oferenda, eu digo, irá frutificar plenamente.</p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Quando uma pessoa desapegada faz para uma pessoa desapegada <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>com o coração confiante uma oferenda obtida corretamente,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>depositando fé em que o fruto da ação será imenso,<br>

<a name=R14></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>essa oferenda, eu digo, é a melhor oferenda
no mundo.” [<a href="#N14">14</a>]<o:p></o:p></span></p>

