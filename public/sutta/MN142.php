<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[1] Mahapajapati Gotami era a irmã mais nova
da Rainha Mahamaya, a mãe do Buda, e era a esposa do Rei Suddhodana. Depois da
morte de Mahamaya, ela se tornou a mãe adotiva do Buda. Este sutta ocorre no
início da carreira de ensino do Buda, quando do seu retorno à sua cidade natal.
Depois da morte do Rei Suddhodana, Mahapajapati insistiu em que o Buda
admitisse mulheres na Sangha e a ordenação dela marca o início da Sangha de
Bhikkhunis, a Ordem das Monjas. [<a href="#R1">Retorna</a>]<o:p></o:p></span></p>

<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[2] MA: O Buda pediu que ela desse a oferenda
para a Sangha porque ele queria que a volição de generosidade fosse dirigida
tanto para a Sangha como para ele mesmo, visto que a volição combinada
produziria mérito que conduziria ao bem-estar e felicidade dela por muito
tempo. Ele também disse isso para que as gerações futuras fossem motivadas a
mostrar respeito para com a Sangha, e suportando a Sangha com os quatro
requisitos físicos contribuiriam para a longevidade dos Ensinamentos. [<a
href="#R2">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[3] Essas são as quatro qualidades de um <i>sotapanna</i>.
Portanto, é evidente que na época em que este sutta ocorreu, Mahapajapati
Gotami já havia entrado na correnteza. [<a href="#R3">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[4] MA: O Buda profere este ensinamento porque
o sutta começou com uma oferenda pessoal para ele, e ele deseja esclarecer o
valor comparativo entre as oferendas pessoais e as oferendas dadas para a
Sangha. [<a href="#R4">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N5></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[5] MA e MT explicam que esse termo pode de
forma geral ser ampliado para incluir tanto um discípulo leigo que buscou refúgio
na Jóia Tríplice, como leigos e monges que estejam empenhados em realizar o
treinamento na virtude e a prática de concentração e insight. No seu sentido
técnico estrito ele se refere apenas aos que possuem o caminho supramundano
de entrar na correnteza. [<a href="#R5">Retorna</a>]<o:p></o:p></span></p>

<p class=Normal><a name=N6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[6] MA: esse é um contemplativo não Budista que
alcança os jhanas e os tipos de conhecimento direto mundanos. [<a href="#R6">Retorna</a>]
<o:p></o:p></span></p>


<p class=Normal><a name=N7></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[7] MA: Em cem existências, proporcionará, ao
que faz a oferenda, vida longa, beleza, felicidade, força e inteligência e fará
com que ele esteja livre da agitação. As realizações seguintes devem ser
entendidas de forma semelhante. [<a href="#R7">Retorna</a>]<o:p></o:p></span></p>


