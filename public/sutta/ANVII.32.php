<p class=Normal> Então, quando a noite estava bem avançada, um certo deva com belíssima aparência que iluminou toda a área do Bosque de Jeta, se aproximou do Abençoado. Ao se aproximar ele homenageou o Abençoado e ficando em pé a um lado o deva disse: </p>

<p class=Normal>"Venerável senhor, há essas sete qualidades que conduzem ao não-declínio de um bhikkhu. Quais sete? Reverenciar o Mestre, reverenciar o Dhamma, reverenciar a Sangha, reverenciar o treinamento, reverenciar a concentração, reverenciar a diligência, e reverenciar a hospitalidade. Essas são as sete qualidades que conduzem ao não-declínio de um bhikkhu." </p>

<p class=Normal>Isso foi o que o deva disse. O Mestre aprovou. Então aquele deva pensando, "O Mestre me aprovou," homenageou o Abençoado, e mantendo-o à sua direita, desapareceu. </p>

<p class=Normal>Então, quando a noite havia terminado, o Abençoado se dirigiu aos bhikkhus: "Ontem à noite, bhikkhus, quando a noite estava bem avançada, um certo deva com belíssima aparência que iluminou toda a área do Bosque de Jeta, se aproximou de mim. Ao se aproximar ele me homenageou e ficando em pé a um lado o deva disse: 'Venerável senhor, há essas sete qualidades que conduzem ao não-declínio de um bhikkhu. Quais sete? Reverenciar o Mestre, reverenciar o Dhamma, reverenciar a Sangha, reverenciar o treinamento, reverenciar a concentração, reverenciar a diligência, e reverenciar a hospitalidade. Essas são as sete qualidades que conduzem ao não-declínio de um bhikkhu.' Isso foi o que o deva disse. Então aquele deva me homenageou, e mantendo-me à sua direita, desapareceu." </p>

<p class=Normal>Respeitar o Mestre,<br>
respeitar o Dhamma. <br>
respeitar profundamente a Sangha, <br>
respeitar a concentração, ardente, <br>
respeitar profundamente o treinamento; <br>
respeitar a diligência, <br>
respeitar a hospitalidade: <br>
esse bhikkhu não irá decair, <br>
ele está próximo de nibbana. </p>


