<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Vendo
dessa forma, o nobre discípulo bem instruído se desencanta com a forma,
desencanta com a sensação, desencanta com a percepção, desencanta com as
formações volitivas, desencanta com a consciência. Desencantado ele se torna desapegado.
Através do desapego a sua mente é libertada. Quando ela está libertada surge o
conhecimento: ‘Libertada.’ Ele compreende que: ‘O nascimento foi destruído, a
vida santa foi vivida, o que deveria ser feito foi feito, não há mais vir a ser
a nenhum estado.’”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Suponha,
Tissa, que houvesse dois homens: um sem habilidade no caminho, o outro com
habilidade no caminho. Aquele sem habilidade no caminho faria ao outro uma
pergunta acerca do caminho, e outro responderia: ‘Venha, bom homem, este é o
caminho. Siga por este caminho durante algum tempo e você irá encontrar uma
bifurcação. Evite o ramo da esquerda e tome o ramo da direita. Siga um pouco
mais adiante e você verá um bosque cerrado. Siga um pouco mais adiante e você
verá um vasto pantanal. Siga um pouco mais adiante e você verá um precipício
escarpado. Siga um pouco mais adiante e você verá uma encantadora extensão de
terreno plano.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu citei
esse símile, Tissa, de forma a transmitir uma idéia. A idéia é a seguinte: ‘O homem
sem habilidade no caminho’: esta é uma designação para uma pessoa comum. ‘O
homem com habilidade no caminho’: esta é uma designação para o Tathagata, o
Arahant, Perfeitamente Iluminado. ‘A bifurcação no caminho’: esta é uma
designação para a dúvida. ‘O ramo esquerdo’: esta é uma designação para o
caminho óctuplo falso; isto é, entendimento incorreto ... concentração
incorreta. ‘O ramo direito’: esta é uma designação para o Nobre Caminho
Óctuplo; isto é,<span style="mso-spacerun: yes">  </span>entendimento correto
... concentração correta. ‘O bosque cerrado’: esta é uma designação para a
ignorância. ‘O vasto pantanal’: esta é uma designação para os prazeres
sensuais. ‘O precipício escarpado’: esta é uma designação para a desesperança
e a raiva. ‘A encantadora extensão de terreno plano’: esta é uma
designação para Nibbana. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Alegre-se,
Tissa! Alegre-se, Tissa! Eu estou aqui para exortar, eu estou aqui para ajudar,
eu estou aqui para instruir!”<o:p></o:p></span></p>



