<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. Agora, naquela ocasião o Venerável Tissa, o primo do Abençoado por parte
do pai, informou um grande número de bhikkhus: “Amigos, sinto meu corpo como se
estivesse drogado, estou desorientado, os ensinamentos perderam a claridade. A
preguiça e o torpor persistem obcecando a minha mente. Eu vivo a vida santa
insatisfeito e tenho dúvidas em relação aos ensinamentos.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então um
grande número de bhikkhus foram até o Abençoado e depois de cumprimentá-lo
sentaram a um lado e relataram o ocorrido. O Abençoado então se dirigiu a um
certo bhikkhu da seguinte forma: “Venha, bhikkhu, diga ao bhikkhu Tissa em meu
nome que o Mestre o chama.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
venerável senhor,” esse bhikkhu respondeu e foi até o Venerável Tissa e disse:
“O Mestre o chama, amigo Tissa.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
amigo,” o Venerável Tissa respondeu e foi até o Abençoado, e depois de
cumprimentá-lo sentou a um lado. O Abençoado então disse: “É verdade, Tissa,
que você informou um grande número de bhikkhus assim: ‘Amigos, sinto meu corpo
como se estivesse drogado ... e tenho dúvidas com relação aos ensinamentos’?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
venerável senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O que você
pensa, Tissa, se alguém não estiver destituído de cobiça pela forma, não
estiver destituído de paixão, desejo, afeição, sede e ambição pela forma,
então, com a mudança e alteração daquela forma, a tristeza, lamentação, dor, angústia
e desespero surgirão?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
venerável senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Muito bem,
Tissa! Assim ocorre, Tissa, com alguém que não esteja destituído de cobiça pela
forma. Se alguém não estiver destituído de cobiça pela sensação ... pela
percepção ... pelas formações volitivas ... pela consciência, não estiver destituído de
paixão, desejo, afeição, sede e ambição pela consciência, então, com a mudança
e alteração daquela consciência, a tristeza, lamentação, dor, angústia e
desespero surgirão?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
venerável senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Muito bem,
Tissa! Assim ocorre, Tissa, com alguém que não esteja destituído de cobiça pela
consciência. Se alguém estiver destituído de cobiça pela forma, estiver
destituído de paixão, desejo, afeição, sede e ambição pela forma, então, com a
mudança e alteração daquela forma, a tristeza, lamentação, dor, angústia e
desespero surgirão?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
venerável senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Muito bem,
Tissa! Assim ocorre, Tissa, com alguém que esteja destituído de cobiça pela
forma. Se alguém estiver destituído de cobiça pela sensação ... pela percepção
... pelas formações volitivas ... pela consciência, estiver destituído de paixão, desejo,
afeição, sede e ambição pela consciência, então com a mudança e alteração
daquela consciência, a tristeza, lamentação, dor, angústia e desespero
surgirão?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
venerável senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Muito bem,
Tissa! Assim ocorre, Tissa, com alguém que esteja destituído de cobiça pela
