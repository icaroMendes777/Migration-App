<p class=Normal> "Bhikkhus, possuindo cinco coisas um bhikkhu que se dedica à atenção plena na respiração em pouco tempo conquista o imperturbável. [<a href="#N1">1</a>] Quais cinco? (1) Aqui um bhikkhu possui poucas tarefas, poucas obrigações, é fácil de ser sustentado, com fácil contentamento com os requisitos para a vida. (2) Ele come pouco e intenciona pela frugalidade com relação à comida. (3) Ele raramente está sonolento e intenciona pela vigilância. (4) Ele aprendeu muito, se recorda daquilo que aprendeu e consolida aquilo que aprendeu. Aqueles ensinamentos que são admiráveis no início, admiráveis no meio, admiráveis no final, com o correto significado e fraseado e que revelam uma vida santa que é completamente perfeita e imaculada – ensinamentos como esses ele os aprendeu bem, se recorda, domina com a linguagem, investigou com a mente e penetrou corretamente com o entendimento. (5) Ele examina até que ponto a sua mente está libertada. Possuindo essas cinco coisas um bhikkhu que se dedica à atenção plena na respiração em pouco tempo conquista o imperturbável." </p>

