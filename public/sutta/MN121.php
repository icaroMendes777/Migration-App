<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião o Abençoado estava em Savatthi, no Parque do Oriente, no
Palácio da mãe de Migara.<o:p></o:p></span></p>



<p class=Normal><a name=T2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2. Então, quando anoiteceu, o venerável Ananda se levantou da meditação,
foi até o Abençoado e, após cumprimentá-lo, sentou a um lado e disse:<o:p></o:p></span></p>



<p class=Normal><a name=T3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>3. “Venerável senhor, em certa ocasião o Abençoado estava no país dos
Sakyas em uma cidade chamada Nagaraka. Lá, venerável senhor, eu ouvi e aprendi
isto dos próprios lábios do Abençoado: ‘Agora, Ananda, com freqüência eu
permaneço no vazio.’ [<a href="#N1">1</a>] <a name=R1></a>Eu ouvi isso da forma
correta, venerável senhor, aprendi da forma correta, prestei atenção da forma
correta, recordei da forma correta?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Com
certeza, Ananda, você ouviu da forma correta, aprendeu da forma correta,
prestou atenção da forma correta e recordou da forma correta. Como antes,
Ananda, também agora com freqüência permaneço no vazio. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. “Ananda,
da mesma forma como este palácio da mãe de Migara está vazio de elefantes,
bois, cavalos e éguas, vazio de ouro e prata, vazio da assembléia de homens e
mulheres e existe apenas a presença desse não vazio, isto é, a unicidade da
Sangha dos bhikkhus; assim também, um bhikkhu – não dando atenção à percepção
do vilarejo, não dando atenção à percepção de pessoas – dá atenção à unicidade da
percepção da floresta. [<a href="#N2">2</a>] <a name=R2></a>A mente dele entra
nessa percepção da floresta e adquire confiança, se estabiliza e se liberta. Ele
compreende assim: ‘Todas as perturbações que possam existir, que dependem da
percepção do vilarejo, essas não estão presentes aqui; todas as perturbações
que possam existir, que dependem da percepção de pessoas, essas não estão
presentes aqui. Existe<span style='mso-spacerun:yes'>  </span>apenas este tanto
de perturbação, isto é, a unicidade da percepção da floresta.’ [<a href="#N3">3</a>]
<a name=R3></a>Ele compreende: ‘Este campo de percepção está vazio da percepção
do vilarejo; este campo de percepção está vazio da percepção de pessoas. Está
presente apenas este não vazio, isto é, a unicidade da percepção da floresta.’
Assim, ele considera como vazio aquilo que não está ali, mas com relação ao
que permanece ali ele compreende da seguinte forma: ‘Isto está presente.’
Portanto, Ananda, essa é a penetração genuína, não distorcida, pura, do
vazio.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. “Outra
vez, Ananda, um bhikkhu - não dando atenção à percepção de pessoas, não dando
atenção à percepção da floresta – dá atenção à unicidade da percepção da terra.
[<a href="#N4">4</a>] <a name=R4></a>A mente dele entra nessa percepção da
terra e adquire confiança, se estabiliza e se liberta. Da mesma forma como a pele
de um touro se torna livre de dobras quando é completamente esticada com cem
pregadores; assim também, um bhikkhu – não dando atenção a nenhum dos sulcos e
vazios desta terra, aos rios e ravinas, às regiões com tocos e espinhos, às
montanhas e terrenos irregulares – dá atenção à unicidade da percepção da
terra. A mente dele entra nessa percepção da terra e adquire confiança,
se estabiliza e se liberta. Ele compreende assim: ‘Todas as perturbações que possam
existir, que dependem da percepção de pessoas, essas não estão presentes aqui;
todas as perturbações que possam existir, que dependem da percepção da
floresta, essas não estão presentes aqui. Existe apenas este tanto de
perturbação, isto é, a unicidade da percepção da terra.’ Ele compreende: ‘Este
campo de percepção está vazio da percepção de pessoas; este campo de percepção
está vazio da percepção da floresta. Está presente apenas este não vazio, isto
é, a unicidade da percepção da terra.’ Assim, ele considera como vazio aquilo
que não está ali, mas com relação ao que permanece ali ele compreende da
seguinte forma: ‘Isto está presente.’ Portanto, Ananda, essa é a penetração
genuína, não distorcida, pura, do vazio.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. “Outra
vez, Ananda, um bhikkhu - não dando atenção à percepção da floresta, não dando
atenção à percepção da terra – dá atenção à unicidade da percepção da base do
espaço infinito. [<a href="#N5">5</a>] <a name=R5></a>A mente dele entra nessa
percepção da base do espaço infinito e adquire confiança, se estabiliza e se liberta. Ele compreende assim: ‘Todas as perturbações que possam existir, que
dependem da percepção da floresta, essas não estão presentes aqui; todas as
perturbações que possam existir, que dependem da percepção da terra, essas não
estão presentes aqui. Existe apenas este tanto de perturbação, isto é, a
unicidade da percepção da base do espaço infinito.’ Ele compreende: ‘Este campo
de percepção está vazio da percepção da floresta; este campo de percepção está
vazio da percepção da terra. Está presente apenas este não vazio, isto é, a
unicidade da percepção da base do espaço infinito.’ Assim, ele considera como
vazio aquilo que não está ali, mas com relação ao que permanece ali ele
compreende da seguinte forma: ‘Isto está presente.’ Portanto, Ananda, essa é a
penetração genuína, não distorcida, pura, do vazio.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. “Outra
vez, Ananda, um bhikkhu - não dando atenção à percepção da terra, não dando
atenção à percepção da base do espaço infinito – dá atenção à unicidade da
percepção da base da consciência infinita. A mente dele entra nessa percepção
da base da consciência infinita e adquire confiança, se estabiliza e se liberta.
Ele compreende assim: ‘Todas as perturbações que possam existir, que dependem
da percepção da terra, essas não estão presentes aqui; todas as perturbações
que possam existir, que dependem da percepção da base do espaço infinito, essas
não estão presentes aqui.<span style='mso-spacerun:yes'>  </span>Existe apenas
este tanto de perturbação, isto é, a unicidade da percepção da base da
consciência infinita.’ Ele compreende: ‘Este campo de percepção está vazio da
percepção da terra; este campo de percepção está vazio da percepção da base do
espaço infinito. Está presente apenas este não vazio, isto é, a unicidade da
percepção da base da consciência infinita.’ Assim, ele considera como vazio
aquilo que não está ali, mas com relação ao que permanece ali ele
compreende da seguinte forma: ‘Isto está presente.’ Portanto, Ananda, essa é a
penetração genuína, não distorcida, pura, do vazio.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. “Outra
vez, Ananda, um bhikkhu - não dando atenção à percepção da base do espaço
infinito, não dando atenção à percepção da base da consciência infinita – dá
atenção à unicidade da percepção da base do nada. A mente dele entra nessa
percepção da base do nada e adquire confiança, se estabiliza e se liberta. Ele
compreende assim: ‘Todas as perturbações que possam existir, que dependem da
percepção da base do espaço infinito, essas não estão presentes aqui; todas as
perturbações que possam existir, que dependem da percepção da base da
consciência infinita, essas não estão presentes aqui. Existe apenas este tanto
de perturbação, isto é, a unicidade da percepção da base do nada.’ Ele
compreende: ‘Este campo de percepção está vazio da percepção da base do espaço
infinito; este campo de percepção está vazio da percepção da base da
consciência infinita. Está presente apenas este não vazio, isto é, a unicidade da
percepção da base do nada.’ Assim, ele considera como vazio aquilo que não está
ali, mas com relação ao que permanece ali ele compreende da seguinte forma:
‘Isto está presente.’ Portanto, Ananda, essa é a penetração genuína, não
distorcida, pura, do vazio.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. “Outra
vez, Ananda, um bhikkhu - não dando atenção à percepção da base da consciência
infinita, não dando atenção à percepção da base do nada – dá atenção à
unicidade da percepção da base da nem percepção, nem não percepção. A mente
dele entra nessa percepção da base da nem percepção, nem não percepção e
adquire confiança, se estabiliza e se liberta. Ele compreende assim: ‘Todas as
perturbações que possam existir, que dependem da percepção da base da
consciência infinita, essas não estão presentes aqui; todas as perturbações que
possam existir, que dependem da percepção da base do nada, essas não estão
presentes aqui. Existe apenas este tanto de perturbação, isto é, a unicidade da
percepção da base da nem percepção, nem não percepção.’ Ele compreende: ‘Este
campo de percepção está vazio da percepção da base da consciência infinita;
este campo de percepção está vazio da percepção da base do nada. Está presente
apenas este não vazio, isto é, a unicidade da percepção da base da nem
percepção, nem não percepção.’ Assim, ele considera como vazio aquilo que não
está ali, mas com relação ao que permanece ali ele compreende da seguinte
forma: ‘Isto está presente.’ Portanto, Ananda, essa é a penetração genuína,
não distorcida, pura, do vazio. <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. “Outra vez,
Ananda, um bhikkhu - não dando atenção à percepção da base do nada, não dando
atenção à percepção da base da nem percepção, nem não percepção – dá atenção à
unicidade da concentração da mente sem sinais. [<a href="#N6">6</a>] <a
name=R6></a>A mente dele entra nessa concentração da mente sem sinais e adquire
confiança, se estabiliza e se liberta. Ele compreende assim: ‘Todas as perturbações
que possam existir, que dependem da percepção da base do nada, essas não estão
presentes aqui; todas as perturbações que possam existir, que dependem da
percepção da base da nem percepção, nem não percepção, essas não estão
presentes aqui.<span style='mso-spacerun:yes'>  </span>Existe apenas este tanto
de perturbação, isto é, aquela conectada com as seis bases que dependem deste
corpo e estão condicionadas pela vida.’ Ele compreende: ‘Este campo de
percepção está vazio da percepção da base do nada; este campo de percepção está
vazio da percepção da base da nem percepção, nem não percepção. Está presente
apenas este não vazio, isto é, aquele conectado com as seis bases que dependem
deste corpo e estão condicionadas pela vida.’ Assim, ele considera como vazio
aquilo que não está ali, mas com relação ao que permanece ali ele
compreende da seguinte forma: ‘Isto está presente.’ Portanto, Ananda, essa é a
penetração genuína, não distorcida, pura, do vazio.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. “Outra
vez, Ananda, um bhikkhu - não dando atenção à percepção da base do nada, não
dando atenção à percepção da base da nem percepção, nem não percepção – dá
atenção à unicidade da concentração sem sinais da mente. A mente dele entra
nessa concentração sem sinais da mente e adquire confiança, se estabiliza e se liberta. Ele compreende assim: ‘Esta concentração sem sinais da mente é
condicionada e produzida pela volição. Mas tudo que é condicionado e produzido
pela volição é impermanente, sujeito à cessação.’ [<a href="#N7">7</a>] <a
name=R7></a>Ao compreender e ver dessa forma, a mente dele está libertada da
impureza do desejo sensual, da impureza de ser/existir, e da impureza da
ignorância. Quando ela está libertada surge o conhecimento: ‘Libertada.’ Ele
compreende que: ‘O nascimento foi destruído, a vida santa foi vivida, o que
deveria ser feito foi feito, não há mais vir a ser a nenhum estado.’ <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. “Ele
compreende assim: ‘Todas as perturbações que possam existir, que dependem da
impureza do desejo sensual, essas não estão presentes aqui; todas as
perturbações que possam existir, dependentes da impureza de ser/existir, essas
não estão presentes aqui; todas as perturbações que possam existir, dependentes
da impureza da ignorância, essas não estão presentes aqui. Existe apenas este
tanto de perturbação, isto é, aquela conectada com as seis bases dependentes
deste corpo e condicionadas pela vida.’ Ele compreende: ‘Este campo de
percepção está vazio da impureza do desejo sensual; este campo de percepção
está vazio da impureza de ser/existir; este campo de percepção está vazio da
impureza da ignorância. Está presente apenas este não vazio, isto é, aquele
conectado com as seis bases dependentes deste corpo e condicionadas pela vida.’
Assim, ele considera como vazio daquilo que não está ali, mas com relação
ao que ali permanece, ele compreende da seguinte forma: ‘Isto está
presente.’ Portanto, Ananda, essa é a penetração genuína, não distorcida,
pura, do vazio, supremo e insuperável. [<a href="#N8">8</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R8></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>13. “Ananda, todos os contemplativos e brâmanes que no passado entraram
e permaneceram no vazio puro, supremo e insuperável, todos entraram e
permaneceram neste mesmo vazio puro, supremo e insuperável. Todos os
contemplativos e brâmanes que no futuro entrarão e permanecerão no vazio puro,
supremo e insuperável, todos entrarão e permanecerão neste mesmo vazio puro,
supremo e insuperável. Todos os contemplativos e brâmanes que no presente
entram e permanecem no vazio puro, supremo e insuperável, todos entram e
permanecem neste mesmo vazio puro, supremo e insuperável. Portanto, Ananda,
você deveria treinar dessa forma: ‘Nós entraremos e permaneceremos no vazio
puro, supremo e insuperável.’”<o:p></o:p></span></p>



<p class=Normal><a name=R31></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Isso foi o que disse o Abençoado. O venerável Ananda ficou satisfeito e
contente com as palavras do Abençoado. <o:p></o:p></span></p>

