<p class=Normal> Então um certo bhikkhu foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse: </p>

<p class=Normal><a name=R1></a>"Venerável senhor, é dito, 'aquele que permanece com o Dhamma, aquele que permanece com o Dhamma.' [<a href="#N1">1</a>] De que modo um bhikkhu permanece com o Dhamma?" </p>

<p class=Normal> (1) "Aqui, bhikkhu, um bhikkhu aprende o Dhamma: sumários, prosa e verso, análises, versos, exclamações, símiles, histórias de vidas passadas, eventos maravilhosos, perguntas e respostas. Ele passa o dia aprendendo o Dhamma mas ele negligencia o isolamento e não se dedica à tranquilidade da mente. Esse é chamado um bhikkhu absorto no aprendizado, não aquele que permanece com o Dhamma. </p>

<p class=Normal> (2) "Novamente, um bhikkhu ensina o Dhamma em detalhe para os outros, tal como ele ouviu e aprendeu. Ele passa o dia comunicando o Dhamma mas ele negligencia o isolamento e não se dedica à tranquilidade da mente. Esse é chamado um bhikkhu absorto na comunicação, não aquele que permanece com o Dhamma. </p>

<p class=Normal> (3) "Novamente, um bhikkhu recita o Dhamma em detalhe tal como ele ouviu e aprendeu. Ele passa o dia recitando o Dhamma mas ele negligencia o isolamento e não se dedica à tranquilidade da mente. Esse é chamado um bhikkhu absorto na recitação, não aquele que permanece com o Dhamma. </p>

<p class=Normal> (4) "Novamente, um bhikkhu pondera, examina, e inspeciona mentalmente o Dhamma tal como ele ouviu e aprendeu. Ele passa o dia pensando no Dhamma mas ele negligencia o isolamento e não se dedica à tranquilidade da mente. Esse é chamado um bhikkhu absorto em pensamentos, não aquele que permanece com o Dhamma. </p>

<p class=Normal> (5) "Aqui, bhikkhu, um bhikkhu aprende o Dhamma: sumários, prosa e verso, análises, versos, exclamações, símiles, histórias de vidas passadas, eventos maravilhosos, perguntas e respostas - mas ele não passa o dia apenas aprendendo o Dhamma. Ele não negligencia o isolamento e se dedica à tranquilidade da mente. É desse modo que um bhikkhu permanece com o Dhamma. </p>

<p class=Normal>"Portanto, bhikkhu, eu ensinei o que é aquele absorto no aprendizado, aquele absorto na comunicação, aquele absorto na recitação, aquele absorto em pensamentos, e aquele que permanece com o Dhamma. Aquilo que por compaixão um Mestre deveria fazer para os seus discípulos, desejando o bem-estar deles, isso eu fiz por você. Ali estão aquelas árvores, aquelas cabanas vazias. Medite, bhikkhu, não adie, ou então você irá se arrepender mais tarde. [<a href="#N2">2</a>] Essa é a nossa instrução para você. </p>

