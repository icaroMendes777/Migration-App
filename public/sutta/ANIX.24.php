<p class=Normal> "Bhikkhus, há essas nove permanências dos seres. Quais nove? </p>

<p class=Normal> (1) "Bhikkhus, há seres que são distintos no corpo e distintos na percepção, como os seres humanos, alguns devas, e alguns seres nos mundos inferiores. Essa é a primeira permanência dos seres. [<a href="#N1">1</a>] </p>

<p class=Normal><a name=R1></a> (2) "Há seres que são distintos no corpo mas idênticos na percepção, como os devas na companhia de Brahma que renascem através do primeiro jhana. Essa é a segunda permanência dos seres. [<a href="#N2">2</a>] </p>

<p class=Normal><a name=R2></a> (3) "Há seres que são idênticos no corpo mas distintos na percepção, como os devas que emanam radiância. Essa é a terceira permanência dos seres. [<a href="#N3">3</a>] </p>

<p class=Normal><a name=R3></a> (4) "Há seres que são idênticos no corpo e idênticos na percepção, como os devas da glória refulgente. Essa é a quarta permanência dos seres. [<a href="#N4">4</a>] </p>

<p class=Normal><a name=R4></a> (5) "Há seres que são não-perceptivos, sem experiências, como os devas que são não-perceptivos. Essa é a quinta permanência dos seres. </p>

<p class=Normal> (6) "Há seres que com a completa superação das percepções da forma, com o desaparecimento das percepções do contato sensorial, sem dar atenção às percepções da diversidade, [percebendo] o ‘espaço é infinito,’ pertencem à base do espaço infinito. Essa é a sexta permanência dos seres. </p>

<p class=Normal> (7) "Há seres que com a completa superação da base do espaço infinito, [percebendo] 'a consciência é infinita,' pertencem à base da consciência infinita. Essa é a sétima permanência dos seres.

<p class=Normal> (8) "Há seres que com a completa superação da base da consciência infinita, [percebendo] ‘não há nada,’ pertencem à base do nada. Essa é a oitava permanência dos seres. </p>

<p class=Normal> (9) "Há seres que com a completa superação da base do nada, pertencem à base da nem percepção, nem não percepção. Essa é a nona permanência dos seres. </p>

<p class=Normal>"Essas, bhikkhus, são as nove permanências dos seres."</p>

