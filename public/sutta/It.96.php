<p class=Normal> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: "Bhikkhus, alguém atado pelo grilhão do desejo sensual e pelo grilhão do apego ao ser/existir é aquele que retorna, que retorna a este estado. Alguém livre do grilhão do desejo sensual mas ainda atado pelo grilhão do apego ao ser/existir é aquele que não retorna, que não retorna a este estado. Alguém livre do grilhão do desejo sensual e livre do grilhão do apego ao ser/existir é um arahant, cujas impurezas foram destruídas."</p>

<p class=Normal style='margin-left:.75in'> Presos por ambos grilhões -<br>
o grilhão do prazer sensual e o grilhão de ser/existir -<br>
os seres vivos continuam no samsara, <br>
seguindo para o nascimento e morte.</p>

<p class=Normal style='margin-left:.75in'> Aqueles que abandonam o desejo sensual, <br>
mas ainda não lograram a destruição das impurezas, <br>
aprisionados pelo grilhão de ser/existir, <br>
são declarados aqueles que não retornam.</p>

<p class=Normal style='margin-left:.75in'> Mas aqueles que eliminaram as dúvidas, <br>
destruíram a presunção e a renovação do ser/existir. <br>
Que destruíram por completo as impurezas, <br>
embora presentes no mundo, foram mais além.</p>



