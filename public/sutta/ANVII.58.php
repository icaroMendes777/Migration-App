<p class=Normal style='margin-left:.75in'>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Certa vez, o
Abençoado estava entre os Bhaggas no Parque do Gamo no Bosque de Bhesakala,
próximo ao Covil dos Crocodilos. Naquela ocasião, o Venerável Maha Moggallana [<a
href="#N1">1</a>] <a name=R1></a>estava sentado, cabeceando, próximo ao
vilarejo de Kallavalaputta, em <st1:place w:st="on"><st1:country-region w:st="on">Magadha</st1:country-region></st1:place>.
O Abençoado, através do olho divino que é purificado e ultrapassa o humano, viu
o Venerável Maha Moggallana sentado, cabeceando, próximo ao vilarejo de Kallavalaputta,
em <st1:country-region w:st="on"><st1:place w:st="on">Magadha</st1:place></st1:country-region>.
Assim que viu aquilo - como um homem forte flexiona o seu braço estendido ou
estende seu braço flexionado – ele desapareceu do Parque do Gamo no Bosque de
Bhesakala, próximo ao Covil dos Crocodilos, e reapareceu próximo ao vilarejo de
Kallavalaputta, em Magadha, em frente ao Venerável Maha Moggallana. Ali ele sentou
num assento que havia sido preparado e disse para o Venerável Maha Moggallana: <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Você está
cabeceando, Moggallana? Você está cabeceando?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
venerável senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
Moggallana, qualquer percepção que você tenha em mente quando a sonolência
surgir, não dê atenção para essa percepção, não insista nisso. É possível que,
ao <span class=GramE>fazer</span> isso, você se livre da sonolência. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas, se ao <span
class=GramE>fazer</span> isso, você não se livrar da sonolência, então traga
para a memória o Dhamma da forma como você o ouviu e memorizou, reexamine e
pondere isso na sua mente.<span style='mso-spacerun:yes'>  </span>É possível
que, ao fazer isso, você se livre da sonolência.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas, se ao <span
class=GramE>fazer</span> isso, você não se livrar da sonolência, então recite em
detalhe o Dhamma da forma como você o ouviu e memorizou. É possível que, ao
fazer isso, você se livre da sonolência.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas, se ao
fazer isso, você não se livrar da sonolência, então pressione os lóbulos das
orelhas e esfregue os membros com as mãos. É possível que, ao fazer isso, você se
livre da sonolência.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas, se ao
fazer isso, você não se livrar da sonolência, então levante do seu assento e
depois de lavar os olhos com água, olhe em volta em todas as direções e para
cima para as estrelas e as constelações. É possível que, ao fazer isso, você se
livre da sonolência.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas, se ao <span
class=GramE>fazer</span> isso, você não se livrar da sonolência, então coloque a
sua atenção na percepção da luz, permaneça resoluto na percepção da luz do dia,
sendo o dia o mesmo que a noite, a noite o mesmo que o dia. Assim com a atenção
aberta e desimpedida desenvolva uma mente luminosa. É possível que, ao fazer
isso, você se livre da sonolência.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas, se ao <span
class=GramE>fazer</span> isso você não se livrar da sonolência, então com os
sentidos imersos internamente, sem que a mente se disperse para o exterior, pratique
a meditação andando, caminhando para cá e para lá, com atenção plena e plena
consciência. É possível que, ao fazer isso, você se livre da sonolência.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas, se ao <span
class=GramE>fazer</span> isso, você não se livrar da sonolência, então deite do
lado direito, na postura do leão, com um pé sobre o outro, atento e plenamente
consciente, após anotar na sua mente o horário para levantar. Assim que
despertar, levante-se com rapidez, com o pensamento, ‘Eu não irei me entregar
ao prazer de ficar deitado, o prazer de estar recostado, o prazer da
sonolência.’ Assim é como você deve treinar.<span style='mso-spacerun:yes'> 
</span><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Além
disso, Moggallana, você deve treinar: ‘Eu não visitarei as famílias com o meu
orgulho <span class=GramE>inflado.</span>’ Assim é como você deve treinar. Nas
famílias há muitas tarefas que precisam ser feitas de modo que as pessoas podem
não dar atenção a um bhikkhu visitante. Se um bhikkhu as visita com o orgulho
inflado, o pensamento poderá lhe ocorrer, ‘Agora, eu me pergunto, quem causou
essa separação entre essa família e eu? Essas pessoas parece que não me querem bem.’ Não obtendo
nada, ele fica perturbado. Perturbado, ele fica inquieto. Inquieto, ele fica descontrolado.
Descontrolado, a sua mente se afasta da concentração. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Além
disso, Moggallana, você deve treinar: ‘Eu não empregarei linguagem <span
class=GramE>contenciosa.</span>’ Assim é como você deve treinar. Quando há
linguagem contenciosa, muita discussão pode ser esperada. Quando há muita
discussão, há inquietação. Aquele que está inquieto fica descontrolado. Descontrolado,
a sua mente se afasta da concentração.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não é o
caso, Moggallana, que eu elogie todo tipo de associação. Mas, não é o caso que
eu critique todo tipo de associação. Eu não elogio a associação com chefes de
família e contemplativos. Mas, quanto a locais onde há poucos sons e pouco ruído,
arejados pela brisa fresca, distante das casas das pessoas, adequado para o
isolamento: eu elogio a associação com locais desse tipo.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isso
foi dito o Venerável Moggallana disse para o Abençoado: “De maneira concisa,
venerável senhor, de que modo um bhikkhu está libertado através do fim do
desejo, absolutamente perfeito, totalmente livre dos grilhões, um discípulo da
perfeita vida santa, completamente consumado: o melhor entre os seres humanos e
divinos?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Há o caso,
Moggallana, em que um bhikkhu aprendeu, ‘Todos os fenômenos não merecem o <span
class=GramE>apego.</span>’ Tendo aprendido que todos os fenômenos não merecem o
apego, ele tem o conhecimento direto de tudo. Tendo o conhecimento direto de
tudo, ele compreende tudo completamente. Tendo compreendido tudo completamente,
então qualquer sensação que ele experimente – prazerosa, dolorosa, nem
prazerosa, nem dolorosa – em relação a essa mesma sensação, ele permanece contemplando
a impermanência, contemplando o desapego, contemplando a cessação, contemplando
o abandono. Permanecendo assim contemplando a impermanência, contemplando o
desapego, contemplando a cessação, contemplando o abandono, com relação àquela
sensação, ele não é sustentado por nada no mundo. [<a href="#N2">2</a>] <a
name=R2></a>Sem sustentação, ele não se agita. Sem agitação ele realiza nibbana.
Ele compreende que: ‘O nascimento foi destruído, a vida santa foi vivida, o que
deveria ser feito foi feito, não há mais vir a ser a nenhum estado.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“É deste modo,
Moggallana, de maneira concisa, que um bhikkhu está libertado através do fim do
desejo, absolutamente perfeito, totalmente livre dos grilhões, um discípulo da
perfeita vida santa, completamente consumado: o melhor entre os seres humanos e
divinos.”<o:p></o:p></span></p>



