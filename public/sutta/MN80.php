<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. </span>Assim
ouvi. Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no
Parque de Anathapindika<span lang=PT-BR style='mso-ansi-language:PT-BR'>.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. Então, o
errante Vekhanassa foi até o Abençoado e ambos se
cumprimentaram. [<a href="#N1">1</a>] <a name=R1></a>Quando a conversa amigável
e cortês havia terminado, ele ficou em pé a um lado e na presença do Abençoado
proferiu a seguinte exclamação:<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Este é o
esplendor perfeito, este é o esplendor perfeito!” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas,
Kaccana, porque você diz: ‘Este é o esplendor perfeito, este é o esplendor
perfeito!’? O que é esse esplendor perfeito?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mestre
Gotama, esse esplendor é o esplendor perfeito que não é superado por nenhum
outro esplendor mais elevado ou mais sublime.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas,
Kaccana, o que é esse esplendor que não é superado por nenhum outro esplendor
mais elevado ou mais sublime?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mestre
Gotama, esse esplendor é o esplendor perfeito que não é superado por nenhum
outro esplendor mais elevado ou mais sublime.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3-11.
“Kaccana, você pode por muito tempo continuar dizendo isso ... (igual ao <a
href="MN79.php#T10">MN 79.10-18</a>) ... mas você não indica que esplendor é
esse.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12.
“Kaccana, </span>existem esses cinco elementos do prazer sensual. [<a href="#N2">2</a>]
<a name=R2></a>Quais cinco? Formas conscientizadas através do olho que são
desejáveis, agradáveis e fáceis de serem gostadas, conectadas com o desejo
sensual e que provocam a cobiça<span lang=PT-BR style='mso-ansi-language:PT-BR'>.
</span>Sons conscientizados através do ouvido … Aromas conscientizados através do nariz …
Sabores conscientizados através da língua … Tangíveis conscientizados através do corpo
que são desejáveis, agradáveis e fáceis de serem gostados, conectados com o
desejo sensual e que provocam a cobiça. Esses são os cinco elementos do prazer
sensual.<span lang=PT-BR style='mso-ansi-language:PT-BR'>.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. “Agora,
Kaccana, o prazer e a alegria que surgem na dependência desses cinco elementos
do prazer sensual são chamados de prazer sensual. Portanto, o prazer sensual
[surge] através dos prazeres sensuais, mas além do prazer sensual há um prazer
mais elevado do que o sensual e ele é declarado como o superior dentre eles.” [<a
href="#N3">3</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>14. Quando isso foi dito, o errante Vekhanassa disse: “É maravilhoso,
Mestre Gotama, é surpreendente, quão bem isso foi expresso pelo Mestre Gotama:
“Portanto, o prazer sensual [surge] através dos prazeres sensuais, mas além do
prazer sensual há um prazer mais elevado do que o sensual e ele é declarado
como o superior dentre eles.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Kaccana,
para você que possui um entendimento distinto, que aceita um outro ensinamento,
que aprova um outro ensinamento, que se dedica a um treinamento distinto, que
segue um mestre distinto, é difícil saber o que é a sensualidade ou o que é o
prazer sensual, ou o que é o prazer mais elevado do que o sensual. Mas aqueles
bhikkhus que são arahants com as impurezas destruídas, que viveram a vida
santa, fizeram o que devia ser feito, depuseram o fardo, alcançaram o objetivo
verdadeiro, destruíram os grilhões da existência e estão completamente libertados
através do conhecimento supremo – são eles que sabem o que é a sensualidade, o
que é o prazer e o que é o prazer mais elevado do que o sensual.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. Quando
isso foi dito, o errante Vekhanassa ficou zangado e irritado, ele insultou,
menosprezou e censurou o Abençoado dizendo: “O contemplativo Gotama será
derrotado.” Ele então disse para o Abençoado: “Então há aqui alguns
contemplativos e brâmanes que sem conhecer o passado e sem ver o futuro, no
entanto dizem: ‘O nascimento está destruído, a vida santa foi vivida, o que
devia ser feito foi feito, não há mais vir a ser a nenhum estado.’ O que eles
dizem acaba se tornando ridículo; acaba se tornando meras palavras, vazias e
ocas.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16. “Se algum
contemplativo ou brâmane, sem conhecer o passado e sem ver o futuro, no entanto
diz: ‘O nascimento está destruído, a vida santa foi vivida, o que devia ser
feito foi feito, não há mais vir a ser a nenhum estado,’ ele pode ser refutado
de acordo com o Dhamma. Melhor, Kaccana, deixe o passado e o futuro de lado.
Que venha um homem sábio, alguém que seja honesto e sincero, um homem íntegro.
Eu o instruirei, eu<span style="mso-spacerun: yes">  </span>ensinarei o Dhamma
para ele de tal modo que, praticando da forma instruída, ele em breve saberá e
verá por si mesmo: ‘Pois, de fato, assim ocorre a libertação do cativeiro, isto
é, do cativeiro da ignorância.’ Suponha, Kaccana, que houvesse um tenro jovem
bebê deitado de costas, atado por quatro ataduras fortes [nos quatro membros] e
a quinta no pescoço; e mais tarde, como resultado do seu crescimento e do
amadurecimento das suas faculdades, essas ataduras se afrouxassem, ele saberia
que ‘Eu estou livre’ e não haveria mais cativeiro. Da mesma maneira, que venha
um homem sábio ... ‘Pois, de fato, assim ocorre a libertação do cativeiro, isto
é, do cativeiro da ignorância.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17. Quando
isso foi dito, o errante Vekhanassa disse para o Abençoado: </span>“Magnífico,
Mestre Gotama! Magnífico, Mestre Gotama! Mestre Gotama esclareceu o Dhamma de
várias formas, como se tivesse colocado em pé o que estava de cabeça para
baixo, revelasse o que estava escondido, mostrasse o caminho para alguém que
estivesse perdido ou segurasse uma lâmpada no escuro para aqueles que possuem
visão pudessem ver as formas. Eu busco refúgio no Mestre Gotama, no Dhamma e na
Sangha dos bhikkhus. Que o Mestre Gotama me aceite como discípulo leigo que
buscou refúgio para o resto da vida.”<span lang=PT-BR
style='mso-ansi-language:PT-BR'><o:p></o:p></span></p>


