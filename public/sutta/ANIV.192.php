<p class=Normal>
&quot;Bhikkhus,
essas quatro características podem ser conhecidas através de quatro (outras)
características. Quais quatro? <o:p></o:p></span></p>


<p class=Normal>
&quot;É vivendo
junto com uma pessoa que a sua virtude pode ser conhecida e somente após um
longo período de tempo, não um período curto; por alguém que seja atento, não
por alguém que seja desatento; por alguém que tenha sabedoria, não por alguém
que não tenha sabedoria.<o:p></o:p></span></p>

<p class=Normal>
&quot;É lidando
com uma pessoa que a sua pureza pode ser conhecida e somente após um longo
período de tempo, não um período curto; por alguém que seja atento, não por
alguém que seja desatento; por alguém que tenha sabedoria, não por alguém que
não tenha sabedoria.<o:p></o:p></span></p>


<p class=Normal>
&quot;É através da
adversidade que a tolerância de uma pessoa pode ser conhecida e somente após um
longo período de tempo, não um período curto; por alguém que seja atento, não
por alguém que seja desatento; por alguém que tenha sabedoria, não por alguém
que não tenha sabedoria.<o:p></o:p></span></p>

<p class=Normal>
&quot;É através
da discussão que a sabedoria de uma pessoa pode ser conhecida e somente após um
longo período de tempo, não um período curto; por alguém que seja atento, não
por alguém que seja desatento; por alguém que tenha sabedoria, não por alguém
que não tenha sabedoria.<o:p></o:p></span></p>


<p class=Normal><b>[1]</b> &quot;’É vivendo junto com uma pessoa que a sua
virtude pode ser conhecida e somente após um longo período de tempo, não um
período curto; por alguém que seja atento, não por alguém que seja desatento;
por alguém que tenha sabedoria, não por alguém que não tenha sabedoria’. Assim
foi dito e em referência a que foi dito isto?<o:p></o:p></span></p>


<p class=Normal>
&quot;É o caso em
que um indivíduo, através da convivência com outro, descobre o seguinte: ‘Por
um longo<span style="mso-spacerun: yes">  </span>tempo esta pessoa através das
suas ações tem sido despedaçada, quebrada, manchada, suja. Ela não tem sido
consistente nas suas ações. Ela não pratica consistentemente de acordo com os
preceitos. Ela é uma pessoa sem princípios, não é uma pessoa virtuosa, com
princípios.’ E então existe o caso em que um indivíduo, através da convivência
com outro, descobre o seguinte: ‘ Por um longo tempo esta pessoa através das
suas ações não tem sido despedaçada, quebrada, manchada, suja. Ela tem sido
consistente nas suas ações. Ela pratica consistentemente de acordo com os
princípios. Ela é virtuosa, uma pessoa de princípios, não uma pessoa sem
princípios.' <o:p></o:p></span></p>


<p class=Normal>
&quot;’É através
da convivência com uma pessoa que a sua virtude pode ser conhecida e somente
após um longo período de tempo, não um período curto; por alguém que seja
atento, não por alguém que seja desatento; por alguém que tenha sabedoria, não
por alguém que não tenha sabedoria’. Assim foi dito e em referência a isso é
que foi dito. <o:p></o:p></span></p>

<p class=Normal><b>[2]</b>
'&quot;É lidando com uma pessoa que a sua pureza pode ser conhecida e somente
após um longo período de tempo, não um período curto; por alguém que seja
atento, não por alguém que seja desatento; por alguém que tenha sabedoria, não
por alguém que não tenha sabedoria.’ Assim foi dito e em referência a que foi
dito isto?<o:p></o:p></span></p>


<p class=Normal>
&quot;É o caso em
que um indivíduo, por lidar com outro, descobre o seguinte: ‘Esta pessoa lida
de uma forma com uma pessoa, de outra forma com duas pessoas, de outra forma
com três pessoas, de outra forma com muitas. As suas primeiras negociações não
estão em harmonia com as subsequentes. Ela é impura no seu comportamento, não
pura.’ E então existe o caso em que um indivíduo, por lidar com outro, descobre
o seguinte: a forma como essa pessoa lida com uma pessoa, é a mesma forma como
ele lida com duas, com três, com muitas. As suas primeiras negociações estão em
harmonia com as subsequentes. Ela é pura no seu comportamento, não impura.’ <o:p></o:p></span></p>


<p class=Normal>
&quot;É lidando
com uma pessoa que a sua pureza pode ser conhecida, e somente após um longo
período de tempo, não um período curto; por alguém que seja atento, não por
alguém que seja desatento; por alguém que tenha sabedoria, não por alguém que
não tenha sabedoria.’ Assim foi dito e em referência a isso é que foi dito. <o:p></o:p></span></p>


<p class=Normal><b>[3]</b> &quot;É através da adversidade que a tolerância de uma pessoa pode ser
conhecida e somente após um longo período de tempo, não um período curto; por
alguém que seja atento, não por alguém que seja desatento; por alguém que tenha
sabedoria, não por alguém que não tenha sabedoria. Assim foi dito e em
referência a que foi dito isto?<o:p></o:p></span></p>


<p class=Normal>
&quot;É o caso em
que um indivíduo, sofrendo a perda de parentes, perda de riqueza, ou perda por
doença, não reflete: 'Assim é a convivência no mundo. Assim é quando se tem uma
identidade pessoal [<i style='mso-bidi-font-style:normal'>atta-bhava,</i>
literalmente “estado do eu&quot;]. Quando se vive no mundo, quando se tem uma
identidade pessoal, essas oito condições mundanas revolvem acompanhando o mundo
e o mundo revolve acompanhando essas oito condições mundanas: ganho e perda,
fama e má reputação, elogio e crítica, alegria e tristeza.’ Sofrendo a perda de
parentes, perda de riqueza, ou perda por doença, ele fica triste, sofre e se
lamenta, bate no peito, fica perturbado. E então existe o caso em que um
indivíduo, sofrendo a perda de parentes, perda de riqueza ou perda por doença,
reflete: ‘Assim é a convivência no mundo. Assim é como se tem uma identidade
pessoal. Quando se vive no mundo, quando se tem uma identidade pessoal, essas
oito condições mundanas revolvem acompanhando o mundo e o mundo revolve
acompanhando essas oito condições mundanas: ganho e perda, fama e má reputação,
elogio e crítica, alegria e tristeza.’ Sofrendo a perda de parentes, perda de
riqueza, ou perda por doença, ele não fica triste, sofre ou lamenta, não bate
no peito ou fica perturbado. <o:p></o:p></span></p>

<p class=Normal>
&quot;É através
da adversidade que a tolerância de uma pessoa pode ser conhecida e somente após
um longo período de tempo, não um período curto; por alguém que seja atento,
não por alguém que seja desatento; por alguém que tenha sabedoria, não por
alguém que não tenha sabedoria Assim foi dito e em referência a isso é que foi
dito <o:p></o:p></span></p>


<p class=Normal><b>[4]</b>
&quot;’É através da discussão que a sabedoria de uma pessoa pode ser conhecida
e somente após um longo período de tempo, não um período curto; por alguém que
seja atento, não por alguém que seja desatento; por alguém que tenha sabedoria,
não por alguém que não tenha sabedoria. Assim foi dito e em referência a que
foi dito isto?<o:p></o:p></span></p>

<p class=Normal>
&quot;É o caso em
que um indivíduo, através da discussão com outro fica sabendo o seguinte: ‘Pela
forma como essa pessoa aborda um assunto, pela forma como ela aplica (a sua
lógica), pela forma como ela responde a uma pergunta, ela é tola, não tem sabedoria.
Por que isso? Ela não faz declarações profundas, tranquilas, refinadas, além do
escopo da conjectura, sutis, dignas dos sábios. Ela não consegue entender o
significado, ensiná-lo, descrevê-lo, revelá-lo, explicá-lo ou torná-lo simples.
Ela é tola, sem sabedoria.’ Como se um homem com boa visão estando em pé na
margem de um lago pudesse ver um pequeno peixe emergir. O seguinte pensamento
lhe ocorreria, ‘Pela forma como esse peixe emerge, pelo quebrar das ondas, pela
sua velocidade, é um peixe pequeno, não é um peixe grande.’ Da mesma forma um
indivíduo, em uma discussão com um outro, sabe o seguinte: Pela forma como essa
pessoa aborda um assunto, pela forma como ela aplica (a sua lógica), pela forma
como ela responde a uma pergunta …ela é tola, não tem sabedoria.’ <o:p></o:p></span></p>


<p class=Normal>
&quot;E então é o
caso em que um indivíduo, discutindo com um outro sabe o seguinte: ‘Pela forma
como essa pessoa aborda um assunto, pela forma como ela aplica (a sua lógica),
pela forma como ela responde a uma pergunta, ela tem sabedoria, ela é sábia.
Por que isso? Ela faz declarações que são profundas, <a name=PVW>tranquilas</a>,
refinadas, além do escopo de conjectura, sutis, dignas dos sábios. Ela consegue
declarar o significado, ensiná-lo, descrevê-lo, revelá-lo, explicá-lo ou
torná-lo simples. Ela tem sabedoria, ela é sábia.’ Como se um homem com boa
visão estando em pé na margem de um lago pudesse ver um pequeno peixe emergir.
O pensamento lhe ocorreria, ‘Pela forma como esse peixe emerge, pelo quebrar
das ondas, pela sua velocidade, é um peixe pequeno, não é um peixe grande.’ Da
mesma forma um indivíduo em uma discussão com um outro, descobre o seguinte:
Pela forma como essa pessoa aborda um assunto, pela forma como ela aplica (a
sua lógica), pela forma como ela responde a uma pergunta …ela tem sabedoria,
ela é sábia.’ <o:p></o:p></span></p>


<p class=Normal>
&quot;’É através
da discussão que a sabedoria de uma pessoa pode ser conhecida e somente após um
longo período de tempo, não um período curto; por alguém que seja atento, não
por alguém que seja desatento; por alguém que tenha sabedoria, não por alguém
que não tenha sabedoria . Assim foi dito e em referência a isso é que foi dito.
<o:p></o:p></span></p>

<p class=Normal>
&quot;Essas
bhikkhus são as quatro características que podem ser conhecidas através de
quatro (outras) características.” <o:p></o:p></span></p>

