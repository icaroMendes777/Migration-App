<p class=Normal> “Bhikkhus, há esses cinco elementos para escapar. [<a href="#N1 ">1</a>] <a name="R1"> </a>Quais cinco? </p>

<p class=Normal> “Há o caso em que a mente de um bhikkhu ao se ocupar com os prazeres sensuais, não salta sobre os prazeres sensuais, [<a href="#N2">2</a>] <a name="R2"> </a>não ganha confiança, firmeza, ou a libertação nos prazeres sensuais. Mas ao se ocupar com a renúncia, [<a href="#N3 ">3</a>] <a name="R3"> </a>a mente dele salta sobre a renúncia, ganha confiança, firmeza, e a libertação na renúncia. Quando a mente dele está bem dirigida, bem desenvolvida, elevada acima dos prazeres sensuais , libertada e desunida dos prazeres sensuais, então, qualquer tipo de impurezas, tormentos, febres que possam surgir na dependência dos prazeres sensuais , ele está libertado disso. Ele não experimenta essa sensação. Isso é chamado a escapatória dos prazeres sensuais. </p>

<p class=Normal> “Além disso, há o caso em que a mente de um bhikkhu ao se ocupar  com a má vontade, [<a href="#N4 ">4</a>] <a name="R4"> </a>não salta sobre a má vontade, não ganha confiança, firmeza, ou a libertação na má vontade. Mas ao se ocupar com a não má vontade, [<a href="#N5">5</a>] <a name="R5"> </a>a mente dele salta sobre a não má vontade, ganha confiança, firmeza, e a libertação na não má vontade. Quando a mente dele está bem dirigida, bem desenvolvida, elevada acima da má vontade, libertada dela e desunida da má vontade, então, qualquer tipo de impurezas, tormentos, febres que possam surgir na dependência da má vontade, ele está libertado disso. Ele não experimenta essa sensação. Isso é chamado a escapatória da má vontade. </p>

<p class=Normal> “Além disso, há o caso em que a mente de um bhikkhu ao se ocupar  com a crueldade,  [<a href="#N6 ">6</a>] <a name="R6"> </a>não salta sobre a crueldade, não ganha confiança, firmeza, ou a libertação na crueldade. Mas ao se ocupar com a não crueldade,  a mente dele salta sobre a não crueldade, ganha confiança, firmeza, e a libertação na não crueldade. Quando a mente dele está bem dirigida, bem desenvolvida, elevada acima da crueldade, libertada dela e desunida da crueldade, então, qualquer tipo de impurezas, tormentos, febres que possam surgir na dependência da crueldade, ele está libertado disso. Ele não experimenta essa sensação. Isso é chamado a escapatória da crueldade. </p>

<p class=Normal> “Além disso, há o caso em que a mente de um bhikkhu ao se ocupar  com a forma, [<a href="#N7">7</a>] <a name="R7"> </a>não salta sobre a forma, não ganha confiança, firmeza, ou a libertação na forma. Mas ao se ocupar com o sem forma,  a mente dele salta sobre o sem forma, ganha confiança, firmeza, e a libertação no sem forma. Quando a mente dele está bem dirigida, bem desenvolvida, elevada acima da forma, libertada dela e desunida da forma, então, qualquer tipo de impurezas, tormentos, febres que possam surgir na dependência da forma, ele está libertado disso. Ele não experimenta essa sensação. Isso é chamado a escapatória da forma. </p>


<p class=Normal> “Além disso, há o caso em que a mente de um bhikkhu ao se ocupar  com a identidade, [<a href="#N8">8</a>] <a name="R8"> </a>não salta sobre a identidade, não ganha confiança, firmeza, ou a libertação na identidade. Mas ao se ocupar com a não identidade,  a mente dele salta sobre a não identidade, ganha confiança, firmeza, e a libertação na não identidade. Quando a mente dele está bem dirigida, bem desenvolvida, elevada acima da identidade, libertada dela e desunida da identidade, então, qualquer tipo de impurezas, tormentos, febres que possam surgir na dependência da identidade, ele está libertado disso. Ele não experimenta essa sensação. Isso é chamado a escapatória da identidade. </p> 

<p class=Normal> “Ele não é obcecado pelo deleite com os prazeres sensuais. Ele não é obcecado pelo deleite com a má vontade. Ele não é obcecado pelo deleite com a crueldade. Ele não é obcecado pelo deleite com a forma. Ele não é obcecado pelo deleite com a identidade. Pela ausência de qualquer obsessão [<a href="#N9 ">9</a>] <a name="R9"> </a>pelos prazeres sensuais , ausência de qualquer obsessão pela má vontade … crueldade … forma … identidade, ele é chamado de um bhikkhu sem apegos. Ele se separou do desejo, rompeu o grilhão e tendo corretamente penetrado a presunção deu um fim ao sofrimento. </p>

<p class=Normal> “Esses, bhikkhus, são os cinco elementos para escapar.” </p>

