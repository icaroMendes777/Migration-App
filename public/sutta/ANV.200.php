<p class=Normal><b>Notas:</b> </p>

<p class=Normal><a name="N1"> </a> [1] <i>Nissaraniyo dhatuyo</i>: oferecem a escapatória dos estados obstrutivos da mente. [<a href="#R1 ">Retorna</a>] </p>

<p class=Normal><a name="N2"> </a> [2] De acordo com os comentários, a contemplação da repulsa dirigida a um objeto sensual depois de emergir de jhana. Para mais detalhes sobre a contemplação da repulsa, veja o <a href="MN10.php#T10">MN 10</a>. [<a href="#R2 ">Retorna</a>] </p>

<p class=Normal><a name="N3"> </a> [3] De acordo com os comentários, a renúncia neste caso se refere ao primeiro jhana. Este oferece uma escapatória temporária, mas se o jhana for usado como base para o insight e for alcançado o estado de não retorno, (<i>anagami</i>), então a escapatória dos prazeres sensuais  será completa. [<a href="#R3 ">Retorna</a>] </p>

<p class=Normal><a name="N4"> </a> [4] <i>Byapada</i>. De acordo com os comentários, a contemplação com amor bondade dirigida a um objeto que causa raiva, aversão, depois de emergir do jhana. [<a href="#R4">Retorna</a>] </p>

<p class=Normal><a name="N5"> </a> [5] <i>Abyapada</i> sinônimo de amor bondade. [<a href="#R5 ">Retorna</a>] </p>

<p class=Normal><a name="N6"> </a> [6] <i>Vihesa</i>. De acordo com os comentários, a contemplação com compaixão depois de emergir do jhana. [<a href="#R6 ">Retorna</a>] </p>

<p class=Normal><a name="N7"> </a> [7] <i>Rupa</i>. De acordo com os comentários, examinando a forma depois de emergir de uma realização imaterial. [ <a href="#R7 ">Retorna</a>] </p>

<p class=Normal><a name="N8"> </a> [8] <i>Sakkaya</i>. De acordo com os comentários, isto se refere ao praticante da meditação vipassana sem os jhanas, (‘dry insight,’ <i>sukkhavipassaka</i>), que dirige a mente para a investigação dos cinco agregados. [<a href="#R8 ">Retorna</a>] </p>

<p class=Normal><a name="N9"> </a> [9] <i>Niranussayo</i>: sem obsessões ou tendências subjacentes em relação a essas cinco coisas: prazeres sensuais, má vontade, etc. De acordo com os comentários, esta afirmação é feita em elogio ao arahant que permanece tendo realizado a cessação, a escapatória da identidade. [<a href="#R9 ">Retorna</a>] </p>

