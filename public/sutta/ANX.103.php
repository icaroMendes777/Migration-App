<p class=Normal> “Da incorreção vem o fracasso, não o sucesso. E como é, bhikkhus, que da incorreção vem o fracasso, não o sucesso? </p>

<p class=Normal> “Numa pessoa com o entendimento incorreto, surge o pensamento incorreto. Numa pessoa com o pensamento incorreto, surge a linguagem incorreta. Numa pessoa com a linguagem incorreta, surge a ação incorreta. Numa pessoa com a ação incorreta, surge o modo de vida incorreto. Numa pessoa com o modo de vida incorreto, surge o esforço incorreto. Numa pessoa com o esforço incorreto, surge a atenção plena incorreta. Numa pessoa com a atençao plena incorreta, surge a concentração incorreta. Numa pessoa com a concentração incorreta, surge o conhecimento incorreto. Numa pessoa com o conhecimento incorreto, surge a libertação incorreta. </p> 

