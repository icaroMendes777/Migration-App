<p class=Normal> “Da correção vem o sucesso, não o fracasso. E como é, bhikkhus, que da correção vem o sucesso, não o fracasso? </p>

<p class=Normal> “Numa pessoa com o entendimento correto, surge o pensamento correto. Numa pessoa com o pensamento correto, surge a linguagem correta. Numa pessoa com a linguagem correta, surge a ação correta. Numa pessoa com a ação correta, surge o modo de vida correto. Numa pessoa com o modo de vida correto, surge o esforço correto. Numa pessoa com o esforço correto, surge a atenção plena correta. Numa pessoa com a atençao plena correta, surge a concentração correta. Numa pessoa com a concentração correta, surge o conhecimento correto. Numa pessoa com o conhecimento correto,  surge a libertação correta. </p>

<p class=Normal> “Assim é como da correção vem o sucesso, não o fracasso.” </p>

