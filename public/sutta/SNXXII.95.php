<p class=Normal style='margin-left:.75in'>


<p class=Normal> 
Em certa ocasião, o Abençoado estava às margens do rio Gânges entre os
Ayojjhans. Lá ele se dirigiu
aos monges desta forma: <o:p></o:p></span></p>



<p class=Normal>
“Bhikkhus, suponham que uma grande massa de espuma estivesse flutuando
nesse rio Gânges e um homem com boa visão a visse, observasse e<span
style="mso-spacerun: yes">  </span>investigasse com cuidado, e esta lhe
pareceria vazia, oca, sem substância. Pois qual substância poderia ser
encontrada numa massa de espuma? Do mesmo modo, bhikkhus, toda e qualquer
forma, quer seja do passado, futuro ou presente; interna ou externa; grosseira
ou sutil; comum ou sublime; próxima ou distante: um bhikkhu a vê, observa e
investiga com cuidado, e ela lhe parece vazia, oca, sem substância. Pois qual
substância poderia ser encontrada na forma?<o:p></o:p></span></p>



<p class=Normal>
“Bhikkhus, suponham que é o outono, quando estivesse chovendo e caindo pesadas
gotas de chuva, uma bolha de água surgisse e desaparecesse na superfície da água. Um
homem com boa visão a visse, observasse e investigasse com cuidado, e esta lhe
pareceria vazia, oca, sem substância. Pois qual substância poderia ser
encontrada numa bolha de água? Do mesmo modo, bhikkhus, toda e qualquer
sensação, quer seja do passado, futuro ou presente; interna ou externa;
grosseira ou sutil; comum ou sublime; próxima ou distante: um bhikkhu a vê,
observa e investiga com cuidado, e ela lhe parece vazia, oca, sem substância.
Pois qual substância poderia ser encontrada na sensação?<o:p></o:p></span></p>



<p class=Normal>
“Bhikkhus, suponham que é o o último mês do verão, ao meio dia, e
surgisse uma trêmula miragem. Um homem com boa visão a visse, observasse e
investigasse com cuidado, e esta lhe pareceria vazia, oca, sem substância. Pois
qual substância poderia ser encontrada numa miragem? Do mesmo modo, bhikkhus,
toda e qualquer percepção, quer seja do passado, futuro ou presente; interna ou
externa; grosseira ou sutil; comum ou sublime; próxima ou distante: um bhikkhu
a vê, observa e investiga com cuidado, e ela lhe parece vazia, oca, sem
substância. Pois qual substância poderia ser encontrada na percepção?<o:p></o:p></span></p>



<p class=Normal>
“Bhikkhus, suponham que um homem precisasse de madeira, procurasse
madeira, perambulasse em busca de madeira, pegasse um machado afiado e entrasse
na floresta. Lá ele veria uma grande bananeira, ereta, jovem, sem frutos. Então
ele a cortasse pela raiz, removesse a coroa e desenrolasse as folhas que
envolvem o tronco; mas à medida que ele desenrolasse as folhas do tronco, ele
não encontraria nem alburno e muito menos madeira. Um homem com boa visão a
visse, observasse e
investigasse com cuidado, e esta lhe pareceria vazia, oca,
sem substância. Pois qual substância poderia ser encontrada em uma bananeira?
Do mesmo modo, bhikkhus, toda e qualquer formação, quer seja do passado, futuro
ou presente; interna ou externa; grosseira ou sutil; comum ou sublime; próxima
ou distante: um bhikkhu a vê, observa e investiga com cuidado, e ela lhe
pareceria vazia, oca, sem substância. Pois qual substância poderia ser
encontrada nas formações?<o:p></o:p></span></p>



<p class=Normal>
“Bhikkhus, suponham que um mágico, ou o aprendiz de um mágico, estivesse
fazendo um truque de mágica numa encruzilhada. Um homem com boa visão visse, observasse e
investigasse aquilo com cuidado, e aquilo lhe pareceria vazio,
oco, sem substância. Pois qual substância poderia ser encontrada num truque de
mágica? Do mesmo modo, bhikkhus, toda e qualquer consciência, quer seja do
passado, futuro ou presente; interna ou externa; grosseira ou sutil; comum ou
sublime; próxima ou distante: um bhikkhu a vê, observa e investiga com cuidado,
e ela lhe parece vazia, oca, sem substância. Pois qual substância poderia ser
encontrada na consciência?<o:p></o:p></span></p>



<p class=Normal>
“ Vendo dessa maneira, o nobre discípulo bem instruído se desencanta
com a forma, se desencanta com a sensação, se desencanta com a percepção, se desencanta
com as formações volitivas, se desencanta com a consciência. Desencantado ele se torna
desapegado. Através do desapego a sua mente é libertada. Quando
ela está libertada surge o conhecimento: ‘Libertada.’ Ele compreende que: ‘O
nascimento foi destruído, a vida santa foi vivida, o que deveria ser feito foi
feito, não há mais vir a ser a nenhum estado.’”<o:p></o:p></span></p>



<p class=Normal>
Isso foi o que o Abençoado disse. Tendo dito isso o Mestre disse mais:<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'>
 “Forma é como uma
massa de espuma; <br>

sensação uma bolha de
água;<br>

percepção uma miragem;<br>

formações volitivas uma
bananeira;<br>

consciência, um truque
de mágica,<br>

assim explica o
Parente do Sol.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>
Não importa como alguém os observe,<br>

e investigue com
cuidado,<br>

eles parecem vazios e
ocos<br>

quando alguém os vê
com cuidado.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>
Com relação a este
corpo<br>

aquele com a profunda
sabedoria ensinou<br>

que com o abandono de
três coisas<br>

a forma é descartada.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>
Quando a vitalidade, calor e consciência<br>

partem deste corpo
físico,<br>

este ali permanece
descartado:<br>

Alimento para outros,
desprovido de volição.<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'>
Assim é esse contínuo,<br>

essa ilusão, sedutor
de tolos.<br>

É ensinado ser um
assassino;<br>

nenhuma essência é
aqui encontrada.<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'>
Um bhikkhu com a
energia desperta,<br>

deve assim encarar os
agregados,<br>

quer seja durante o
dia ou à noite,<br>

compreendendo, sempre
com atenção plena.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>
Ele deve descartar
todos os grilhões<br>

e fazer um refúgio
para si mesmo;<br>

deve viver como se sua
cabeça estivesse em chamas,<br>

ansiando pelo imortal.”<o:p></o:p></span></p>


