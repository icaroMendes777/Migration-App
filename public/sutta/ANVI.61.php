<p class=Normal> Assim ouvi. Em certa ocasião o Abençoado estava em Benares no Parque do Gamo em Isipatana. Agora naquela ocasião, depois de haver esmolado alimentos e haver retornado, após a refeição, um grupo de bhikkhus sêniores estavam reunidos no pavilhão quando surgiu esta conversa: "Foi dito pelo Abençoado, amigos, no Parayana, nas 'Perguntas de Metteya' [<a href="#N1">1</a>]: </p>

<p class=Normal style='margin-left:.75in'><a name=R1></a>"Tendo compreendido ambos lados, <br>
o sábio não fica preso no meio. <br>
Ele é um grande homem: <br>
aquele que superou o pregador?” </p>

<p class=Normal> "O que amigos é um lado? O que é o segundo lado? O que é o meio? O que é o pregador?" </p>

<p class=Normal> (1) Quando isto foi dito, um certo bhikkhu disse para os bhikkhus sêniores: "Contato, amigos é um lado, o surgimento do contato é o outro lado, a cessação do contato é o meio, o desejo é o pregador. Pois o desejo prega à produção deste ou daquele estado de ser/existir. É desse modo que um bhikkhu conhece de modo direto aquilo que deve ser conhecido diretamente; compreende por completo aquilo que deve ser compreendido completamente; e fazendo isso, nesta mesma vida, dá um fim ao sofrimento." [<a href="#N2">2</a>]</p>

<p class=Normal><a name=R2></a> (2) Quando isto foi dito, um outro bhikkhu disse para os bhikkhus sêniores: "O passado, amigos é um lado, o futuro é o outro lado, o presente é o meio, o desejo é o pregador. Pois o desejo prega à produção deste ou daquele estado de ser/existir. É desse modo que um bhikkhu conhece de modo direto aquilo que deve ser conhecido diretamente; compreende por completo aquilo que deve ser compreendido completamente; e fazendo isso, nesta mesma vida, dá um fim ao sofrimento." </p>

<p class=Normal> (3) Quando isto foi dito, um outro bhikkhu disse para os bhikkhus sêniores: "As sensações prazerosas, amigos é um lado, as sensações dolorosas é o outro lado, as sensações nem prazerosas, nem dolorosas é o meio, o desejo é o pregador. Pois o desejo prega à produção deste ou daquele estado de ser/existir. É desse modo que um bhikkhu conhece de modo direto aquilo que deve ser conhecido diretamente; compreende por completo aquilo que deve ser compreendido completamente; e fazendo isso, nesta mesma vida, dá um fim ao sofrimento." </p>

<p class=Normal> (4) Quando isto foi dito, um outro bhikkhu disse para os bhikkhus sêniores: "A mentalidade (nome), amigos é um lado, materialidade (forma) é o outro lado, consciência é o meio, o desejo é o pregador. Pois o desejo prega à produção deste ou daquele estado de ser/existir. É desse modo que um bhikkhu conhece de modo direto aquilo que deve ser conhecido diretamente; compreende por completo aquilo que deve ser compreendido completamente; e fazendo isso, nesta mesma vida, dá um fim ao sofrimento." </p>

<p class=Normal> (5) Quando isto foi dito, um outro bhikkhu disse para os bhikkhus sêniores: "As seis bases internas, amigos é um lado, as seis bases externas é o outro lado, consciência é o meio, o desejo é o pregador. Pois o desejo prega à produção deste ou daquele estado de ser/existir. É desse modo que um bhikkhu conhece de modo direto aquilo que deve ser conhecido diretamente; compreende por completo aquilo que deve ser compreendido completamente; e fazendo isso, nesta mesma vida, dá um fim ao sofrimento." </p>

<p class=Normal> (6) Quando isto foi dito, um outro bhikkhu disse para os bhikkhus sêniores: "Identidade, amigos é um lado, a origem da identidade é o outro lado, a cessação da identidade é o meio, o desejo é o pregador. Pois o desejo prega à produção deste ou daquele estado de ser/existir. É desse modo que um bhikkhu conhece de modo direto aquilo que deve ser conhecido diretamente; compreende por completo aquilo que deve ser compreendido completamente; e fazendo isso, nesta mesma vida, dá um fim ao sofrimento." </p>

<p class=Normal> Quando isso foi dito, um certo bhikkhu disse para os bhikkhus sêniores: “Amigos, nós todos falamos de acordo com a nossa própria inspiração. Vamos até o Abençoado relatar este assunto. Da forma como o Abençoado responder, assim nos recordaremos.” – “Sim, amigos,” eles responderam. Então aqueles veneráveis foram até o Abençoado e depois de cumprimentá-lo sentaram a um lado e relataram toda a conversa, perguntando: “Venerável senhor, qual de nós falou bem?” </p>

<p class=Normal> [O Abençoado respondeu:] “Todos vocês falaram bem, cada um a seu modo, mas ouçam e prestem bem atenção pois vou relatar minha intenção quando eu disse no Parayana, nas 'Perguntas de Metteya': </p>

<p class=Normal style='margin-left:.75in'>"Tendo compreendido ambos lados, <br>
o sábio não fica preso no meio. <br>
Ele é um grande homem: <br>
aquele que superou o pregador?'” </p>

<p class=Normal> "Sim, venerável senhor," os bhikkhus responderam. O Abençoado disse o seguinte: </p>

<p class=Normal> "Contato, bhikkhus, é um lado, o surgimento do contato é o outro lado, a cessação do contato é o meio, o desejo é o pregador. Pois o desejo prega à produção deste ou daquele estado de ser/existir. É desse modo que um bhikkhu conhece de modo direto aquilo que deve ser conhecido diretamente; compreende por completo aquilo que deve ser compreendido completamente; e fazendo isso, nesta mesma vida, dá um fim ao sofrimento."</p>


