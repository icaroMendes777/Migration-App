<p class=Tit3 align=center style='text-align:center'><b>Samyutta Nikaya LI.15</b></Tit3>
<p class=Tit1 align=center style='text-align:center'>Brahmana Sutta </Tit1>
<p class=Tit1 align=center style='text-align:center'>Para Unnabha o Brâmane </Tit1>

<p class=Normal align=center style='text-align:center'><span lang=PT-BR style='font-size:
