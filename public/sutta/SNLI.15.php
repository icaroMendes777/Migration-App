<p class=Normal style='margin-left:.75in'>

<p class=Normal>Assim ouvi. Em certa ocasião o Ven. Ananda estava em Kosambi, no Parque
de Ghosita. Então, o Brâmane Unnabha foi até o Ven. Ananda e ambos se
cumprimentaram. Quando a conversa cortês e amigável havia terminado ele sentou
a um lado e disse: <o:p></o:p></span></p>


<p class=Normal> &quot;Com qual propósito, Mestre Ananda, a vida santa é vivida sob o
contemplativo Gotama?&quot; <o:p></o:p></span></p>

<p class=Normal> <span lang=PT-BR>&quot;É com o propósito de abandonar o desejo, Brâmane, que
a vida santa é vivida sob o Abençoado.&quot; </span></p>

<p class=Normal> <span lang=PT-BR>&quot;Mas, Mestre Ananda, há um caminho, há um meio, para o
abandono desse desejo?&quot; </span></p>

<p class=Normal> <span lang=PT-BR>&quot;Há um caminho, brâmane, há um meio para o abandono
desse desejo.&quot; </span></p>

<p class=Normal> <span lang=PT-BR>&quot;Mas, Mestre Ananda, qual é o caminho, qual é o meio
para o abandono desse desejo?&quot; </span></p>

<p class=Normal> <span lang=PT-BR>&quot;Neste caso, brâmane, um bhikkhu desenvolve a base do poder
espiritual que possui concentração devido ao desejo e às formações volitivas do
esforço. Ele desenvolve a base do poder espiritual que possui concentração
devido à energia ... devido à mente ... devido à investigação e às formações
volitivas do esforço. Esse, Brâmane, é o caminho, esse é o modo para o abandono
desse desejo.&quot; </span></p>

<p class=Normal> <span lang=PT-BR>&quot;Se é assim, Mestre Ananda, então é um caminho sem
fim, e não com um fim. É impossível que alguém possa abandonar o desejo através
do desejo.&quot; </span></p>

<p class=Normal> <span lang=PT-BR>&quot;Nesse caso, brâmane, eu lhe farei uma contra pergunta. Responda como quiser. O que você pensa: Você antes
não teve o desejo, 'Eu irei ao parque,' e depois que você chegou ao parque,
esse desejo em particular não decaiu?&quot; </span></p>

<p class=Normal> <span lang=PT-BR>&quot;Sim, senhor.&quot; </span></p>

<p class=Normal> <span lang=PT-BR>&quot; Você antes não despertou a energia, pensando, 'Eu
irei ao parque,' e depois que você chegou ao parque, essa energia não
decaiu?&quot; </span></p>

<p class=Normal> <span lang=PT-BR>&quot;Sim, senhor.&quot; </span></p>

<p class=Normal> <span lang=PT-BR>&quot; Você antes não teve que tomar uma decisão, 'Eu irei
ao parque,' e depois que você chegou ao parque, essa decisão não decaiu?&quot; </span></p>

<p class=Normal> <span lang=PT-BR>&quot;Sim, senhor.&quot; </span></p>

<p class=Normal> <span lang=PT-BR>&quot; Você antes não teve que fazer uma investigação,
'Devo ir ao parque?' e depois que você chegou ao parque, essa investigação não
decaiu?&quot; </span></p>

<p class=Normal> <span lang=PT-BR>&quot;Sim, senhor.&quot; </span></p>

<p class=Normal> <span lang=PT-BR>&quot;É exatamente o mesmo, brâmane, com um bhikkhu que é
um Arahant, com as impurezas destruídas, que viveu a vida santa, fez o que
devia ser feito, depôs o fardo, alcançou o verdadeiro objetivo, destruiu os
grilhões da existência e está completamente libertado através do conhecimento
supremo. Ele antes tinha o desejo de alcançar o estado de arahant, e ao ter
alcançado o estado de arahant o desejo correspondente decaiu. Ele antes tinha
despertado a energia para alcançar o estado de arahant, e ao ter alcançado o
estado de arahant a energia correspondente decaiu. Ele antes tinha tomado a decisão
de alcançar o estado de arahant, e ao ter alcançado o estado de arahant a
decisão correspondente decaiu. Ele antes tinha feito uma investigação para
alcançar o estado de arahant, e ao ter alcançado o estado de arahant a
investigação correspondente decaiu. </span></p>

<p class=Normal> <span lang=PT-BR>“O que você pensa, brâmane, sendo esse o caso, este é um
caminho sem fim, ou um caminho com um fim?&quot; </span></p>

<p class=Normal> <span lang=PT-BR>&quot;Com certeza, Mestre Ananda, em sendo esse o caso,
este é um caminho com um fim, e não um caminho sem fim. Magnífico, Mestre
Ananda! Magnífico, Mestre Ananda! Mestre Ananda esclareceu o Dhamma de várias
formas, como se tivesse colocado em pé o que estava de cabeça para baixo,
revelasse o que estava escondido, mostrasse o caminho para alguém que estivesse
perdido ou segurasse uma lâmpada no escuro para aqueles que possuem visão
pudessem ver as formas. Eu busco refúgio no Mestre Gotama, no Dhamma e na
Sangha dos bhikkhus. Que o Mestre Ananda me aceite como o discípulo
leigo que buscou refúgio para o resto da vida. &quot; </span></p>

