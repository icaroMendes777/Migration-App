<p class=Normal> Em Savatthi. Então, um certo bhikkhu foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse: </p>

<p class=Normal> “Venerável senhor, dizem, ‘um orador do Dhamma, um orador do Dhamma.’ De que forma, venerável senhor, alguém é um orador do Dhamma?” </p>

<p class=Normal> “Bhikkhu, se um bhikkhu ensina o Dhamma tendo como propósito o desencantamento em relação ao envelhecimento e morte, pelo seu desaparecimento e cessação, ele pode ser chamado um bhikkhu que é um orador do Dhamma. Se um bhikkhu pratica tendo como propósito o desencantamento em relação ao envelhecimento e morte, pelo seu desaparecimento e cessação, ele pode ser chamado um bhikkhu que está praticando de acordo com o Dhamma. Se, através do desencantamento com o envelhecimento e morte, através do seu desaparecimento e cessação, ele estiver libertado através do desapego, ele pode ser chamado um bhikkhu que realizou Nibbana nesta mesma vida. </p>

<p class=Normal> “Bhikkhu, se alguém ensina o Dhamma tendo como propósito o desencantamento em relação ao nascimento ... à ignorância, pelo seu desaparecimento e cessação, ele pode ser chamado um bhikkhu que é um orador do Dhamma. Se alguém pratica tendo como propósito o desencantamento em relação ao nascimento ... à ignorância, pelo seu desaparecimento e cessação, ele pode ser chamado um bhikkhu que está praticando de acordo com o Dhamma. Se, através do desencantamento com o nascimento ... a ignorância, através do seu desaparecimento e cessação, ele estiver libertado através do desapego, ele pode ser chamado um bhikkhu que realizou Nibbana nesta mesma vida." </p>

