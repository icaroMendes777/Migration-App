<p class=Normal>Assim ouvi. Em certa ocasião, o Abençoado estava viajando em Malla com uma grande sangha de bhikkhus, chegando até a vila brâmane de Thuna. Os brâmanes chefes de família de Thuna ouviram: “Gotama o contemplativo, o filho dos Sakyas, que adotou a vida santa deixando o clã dos Sakyas, andava viajando em Malla com um grande número de bhikkhus, chegou em Thuna." Eles então encheram o poço, até a borda, com grama e palha, pensando, "Não deixem estes contemplativos carecas conseguir água para beber.”</p>



<p class=Normal>Então o Abençoado, afastando-se da estrada, foi até uma certa árvore e sentou-se num assento que havia sido preparado. Sentado, ele disse ao venerável Ananda: “Ananda, traga-me um pouco de água daquele poço.”</p>



<p class=Normal>Quando isto foi dito, venerável Ananda respondeu, "Agora mesmo, venerável senhor, os brâmanes chefes de família de Thuna encheram o poço, até a borda, com grama e palha, pensando, "Não deixem estes contemplativos carecas conseguir água para beber.”</p>



<p class=Normal>Então, uma segunda vez o Abençoado, afastando-se da estrada, foi até uma certa árvore e sentou-se num assento que havia sido preparado. Sentado, ele disse ao venerável Ananda: “Ananda, traga-me um pouco de água daquele poço.”</p>



<p class=Normal>E uma segunda vez o venerável Ananda respondeu, "Agora mesmo, venerável senhor, os brâmanes chefes de família de Thuna encheram o poço, até a borda, com grama e palha, pensando, "Não deixem estes contemplativos carecas conseguir água para beber.”</p>



<p class=Normal>Então, uma terceira vez o Abençoado, afastando-se da estrada, foi até uma certa árvore e sentou-se num assento que havia sido preparado. Sentado, ele disse ao venerável Ananda: “Ananda, traga-me um pouco de água daquele poço.”</p>



<p class=Normal>"Sim, venerável senhor." respondeu o venerável Ananda, e tomando a tigela ele foi até o poço.</p> 



<p class=Normal>Agora, enquanto o venerável Ananda se aproximava, o poço expeliu toda a grama e palha, preenchendo-se até a boca, com água pura, limpa e cristalina, como se estivesse transbordando. O seguinte pensamento ocorreu ao venerável Ananda "É admirável! É maravilhoso! O grande poder e habilidade do Tathagata - enquanto eu me aproximava do poço, este expeliu toda a grama e palha, preenchendo-se até a boca, com água pura e cristalina.”</p>



<p class=Normal>Tomando aquela água na tigela, ele foi até o Abençoado e ao se aproximar disse: "É admirável! É maravilhoso venerável senhor! O grande poder e habilidade do Tathagata - enquanto eu me aproximava do poço, este expeliu toda a grama e palha, preenchendo-se até a boca, com água pura e cristalina. Beba a água, ó Abençoado! Beba a água, ó bem-aventurado.”</p>



<p class=Normal>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p>



	<p class=Normal style='margin-left:.75in'>"Qual o uso de um poço<br>

	se água não faltasse?<br>

	Tendo cortado pela raiz o apego<br>

	em busca do que alguém teria de perambular?”</p>

