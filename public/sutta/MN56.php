<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Em certa
ocasião o Abençoado estava em Nalanda, no mangueiral de Pavarika.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. Agora,
naquela ocasião o Nigantha Nataputta estava em Nalanda com um grande grupo de
Niganthas. Então, depois do Nigantha [chamado] Digha Tapassi [<a href="#N1">1</a>]
<a name=R1></a>haver esmolado em Nalanda e de haver retornado, após a refeição,
ele foi até o mangueiral de Pavarika para ver o Abençoado e ambos se
cumprimentaram. Quando a conversa amigável e cortês havia terminado, ele ficou
em pé a um lado e o Abençoado disse: “Há assentos, Tapassi, sente se assim
desejar.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. Quando
isso foi dito, Digha Tapassi tomou um assento mais baixo e sentou a um lado.
Então o Abençoado perguntou: “Tapassi, quantos tipos de ação o Nigantha
Nataputta descreve para a realização de uma ação prejudicial, para a
perpetração de uma ação prejudicial?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigo Gotama,
o Nigantha Nataputta não tem o costume de usar a descrição ‘ação, ação’; o
Nigantha Nataputta tem o costume de usar a descrição ‘vara, vara’.” [<a
href="#N2">2</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Então, Tapassi, quantos tipos de varas o Nigantha Nataputta descreve
para a a realização de uma ação prejudicial, para a perpetração de uma ação
prejudicial?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigo
Gotama, o Nigantha Nataputta descreve três tipos de varas para a a realização
de uma ação prejudicial, para a perpetração de uma ação prejudicial; isto é, a
vara corporal, a vara verbal e a vara mental.” [<a href="#N3">3</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Como então, Tapassi, a vara corporal é uma, a vara verbal outra e a
vara mental uma outra mais?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“A vara
corporal é uma, amigo Gotama, a vara verbal outra e a vara mental uma outra
mais.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Desses
três tipos de vara, Tapassi, assim analisadas e diferenciadas, qual tipo de
vara o Nigantha Nataputta descreve como sendo a mais repreensível para a
realização de uma ação prejudicial, para a perpetração de uma ação prejudicial:
a vara corporal, ou a vara verbal, ou a vara mental?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Desses
três tipos de vara, amigo Gotama, assim analisadas e diferenciadas, o Nigantha
Nataputta descreve a vara corporal como sendo a mais repreensível para a
realização de uma ação prejudicial, para a perpetração de uma ação prejudicial,
e não tanto a vara verbal e a vara mental.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Você disse
a vara corporal, Tapassi?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu disse a
vara corporal, amigo Gotama.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Você disse
a vara corporal, Tapassi?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu disse a
vara corporal, amigo Gotama.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Você disse
a vara corporal, Tapassi?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu disse a
vara corporal, amigo Gotama.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim o
Abençoado fez com que o Nigantha Digha Tapassi mantivesse a sua afirmação pela
terceira vez. <o:p></o:p></span></p>



<p class=Normal><a name=T4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>4. Então o Nigantha Digha Tapassi perguntou ao Abençoado: “E você, amigo
Gotama, quantos tipos de vara descreve para a realização de uma ação
prejudicial, para a perpetração de uma ação prejudicial?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Tapassi, o
Tathagata não tem o costume de usar a descrição ‘vara, vara’; o Tathagata tem o
costume de usar a descrição ‘ação, ação’.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas, amigo
Gotama, quantos tipos de ação você descreve para a realização de uma ação
prejudicial, para a perpetração de uma ação prejudicial?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Tapassi,
eu descrevo três tipos de ação para a realização de uma ação prejudicial, para
a perpetração de uma ação prejudicial: isto é, a ação corporal, a ação verbal e
a ação mental.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Como
então, mestre Gotama, a ação corporal é uma, a ação verbal outra e a ação
mental uma outra mais?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“A ação
corporal é uma, Tapassi, a ação verbal outra e a ação mental uma outra mais.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Desses
três tipos de ação, mestre Gotama, assim analisadas e diferenciadas, qual tipo
de ação o mestre Gotama descreve como sendo a mais repreensível para a
realização de uma ação prejudicial, para a perpetração de uma ação prejudicial:
a ação corporal, ou a ação verbal, ou a ação mental?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Desses
três tipos de ação, Tapassi, assim analisadas e diferenciadas, eu descrevo a
ação mental como sendo a mais repreensível para a realização de uma ação
prejudicial, para a perpetração de uma ação prejudicial, e não tanto a ação
corporal e a ação verbal.”[<a href="#N4">4</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Você disse a ação mental, amigo Gotama?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu disse a
ação mental, Tapassi.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Você disse
a ação mental, amigo Gotama?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu disse a
ação mental, Tapassi.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Você disse
a ação mental, amigo Gotama?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu disse a
ação mental, Tapassi.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim o
Nigantha Digha Tapassi fez com que o Abençoado mantivesse a sua afirmação pela
terceira vez, depois do que, ele se levantou do seu assento e foi até o
Nigantha Nataputta.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. Agora,
naquela ocasião o Nigantha Nataputta estava sentado junto com uma grande
assembléia de homens leigos de Balaka, sendo Upali o mais destacado dentre
eles. O Nigantha Nataputta viu o Nigantha Digha Tapassi vindo à distância e
perguntou: “De onde você vem agora, ao meio do dia, Tapassi?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu venho
da presença do contemplativo Gotama, venerável senhor.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Você teve
uma conversa com o contemplativo Gotama, Tapassi?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu tive
uma conversa com o contemplativo Gotama, venerável senhor.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Como foi a
sua conversa com ele, Tapassi?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o
Nigantha Digha Tapassi relatou ao Nigantha Nataputta toda a conversa com o
Abençoado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. Quando
isso foi dito, o Nigantha Nataputta disse:<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Muito bem,
Tapassi! O Nigantha Digha Tapassi respondeu ao contemplativo Gotama como um
discípulo bem instruído que compreende corretamente os ensinamentos do seu
mestre. Qual a importância da trivial vara mental em comparação com a grosseira
vara corporal? Ao contrário, a vara corporal é a mais repreensível para a
realização de uma ação prejudicial, para a perpetração de uma ação prejudicial,
e não tanto a vara verbal e a vara mental.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. Quando
isso foi dito, o chefe de família Upali disse para o<span
style='mso-spacerun:yes'>  </span>Nigantha Nataputta: “Muito bem, venerável
senhor, [por parte do] Digha Tapassi! O venerável Tapassi respondeu ao
contemplativo Gotama como um discípulo bem instruído que compreende
corretamente os ensinamentos do seu mestre. Qual a importância da trivial vara
mental em comparação com a<span style='mso-spacerun:yes'>  </span>grosseira
vara corporal? Ao contrário, a vara corporal é a mais repreensível para a
realização de uma ação prejudicial, para a perpetração de uma ação prejudicial,
e não tanto a vara verbal e a vara mental. Agora, venerável senhor, devo ir e
refutar a doutrina do contemplativo Gotama com base nessa afirmação. Se o
contemplativo Gotama afirmar perante mim aquilo que o venerável Digha Tapassi
fez com que ele afirmasse, então da mesma forma como um homem forte [<a
href="#N5">5</a>] <a name=R5></a>é capaz de agarrar os pêlos de um carneiro
peludo e arrastá-lo para cá e para lá e arrastá-lo em círculos, assim em debate
eu irei arrastar o contemplativo Gotama para cá e para lá e em círculos. Da
mesma forma como um forte cervejeiro é capaz de arremessar uma peneira grande
num tanque de água profundo e agarrando-a pelos cantos é capaz de arrastá-la
para cá e para lá e em círculos, assim em debate eu irei arrastar o
contemplativo Gotama para cá e para lá e em círculos. Da mesma forma como um
forte cervejeiro é capaz de tomar um filtro pelos cantos e agitá-lo para cima e
para baixo e dar-lhe pancadas, assim em debate eu agitarei o contemplativo
Gotama para cima e para baixo e darei pancadas nele. E da mesma forma como um
elefante sexagenário é capaz de mergulhar num lago profundo e desfrutar o jogo
de lavar o cânhamo, assim eu desfrutarei o jogo de lavar o cânhamo com o
contemplativo Gotama. Venerável senhor, devo ir e refutar<span
style='mso-spacerun:yes'>  </span>a doutrina do contemplativo Gotama com base
nessa afirmação.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Vá, chefe
de família e refute a<span style='mso-spacerun:yes'>  </span>doutrina do
contemplativo Gotama com base nessa afirmação. Pois, ou eu devo refutar a
doutrina do contemplativo Gotama, ou então o Nigantha Digha Tapassi, ou você
mesmo.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. Quando
isso foi dito, o Nigantha Digha Tapassi disse para o Nigantha Nataputta:
“Venerável senhor, eu não penso que o chefe de família Upali deveria [tentar]
refutar a doutrina do contemplativo Gotama. Pois o contemplativo Gotama é um
mágico que conhece uma mágica de conversão através da qual ele converte os
discípulos de outras seitas.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“É impossível,
Tapassi, não pode acontecer que o chefe de família Upali se torne um discípulo
do contemplativo Gotama; mas é possível, pode acontecer que o contemplativo
Gotama se torne um discípulo do chefe de família Upali. Vá, chefe de família, e
refute a doutrina do contemplativo Gotama. Pois, ou eu devo refutar a doutrina
do contemplativo Gotama, ou então o Nigantha Digha Tapassi, ou você mesmo. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Pela
segunda vez ... Pela terceira vez o Nigantha Digha Tapassi disse para o
Nigantha Nataputta: “Venerável senhor, eu não penso que o chefe de família
Upali deveria [tentar] refutar a doutrina do contemplativo Gotama. Pois o
contemplativo Gotama é um mágico que conhece uma mágica de conversão através da
qual ele converte os discípulos de outras seitas.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“É
impossível, Tapassi, não pode acontecer que o chefe de família Upali se torne
um discípulo do contemplativo Gotama; mas é possível, pode acontecer que o
contemplativo Gotama se torne um discípulo do chefe de família Upali. Vá, chefe
de família, e refute a doutrina do contemplativo Gotama. Pois, ou eu devo
refutar a doutrina do contemplativo Gotama, ou então o Nigantha Digha Tapassi,
ou você mesmo.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. “Sim,
venerável senhor, o chefe de família Upali respondeu e levantando-se do seu
assento, depois de homenagear o Nigantha Nataputta, mantendo-o à sua direita,
ele partiu para ir até o Abençoado no mangueiral de Pavarika. Lá, depois de
cumprimentar o Abençoado, ele sentou a um lado e perguntou: “Venerável senhor,
o Nigantha Digha Tapassi esteve aqui?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O Nigantha
Digha Tapassi esteve aqui, chefe de família.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, você teve uma conversa com ele?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu tive
uma conversa com ele, chefe de família.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Como foi a
sua conversa com ele, venerável senhor?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o
Abençoado relatou ao chefe de família Upali toda a conversa com o Nigantha
Digha Tapassi.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. Quando
isso foi dito, o chefe de família Upali disse para o Abençoado: “Muito bem,
venerável senhor, por parte do Digha Tapassi! O Nigantha Digha Tapassi
respondeu ao contemplativo Gotama como um discípulo bem instruído que
compreende corretamente os ensinamentos do seu mestre. Qual a importância da
trivial vara mental em comparação com a<span style='mso-spacerun:yes'> 
</span>grosseira vara corporal? Ao contrário, a vara corporal é a mais
repreensível para a realização de uma ação prejudicial, para a perpetração de
uma ação prejudicial, e não tanto a vara verbal e a vara mental.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Chefe de
família, se você quiser debater com base na verdade, poderemos ter uma conversa
sobre isso.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu
debaterei com base na verdade, venerável senhor, então conversemos sobre isso.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. “O que
você pensa, chefe de família? Aqui, algum Nigantha pode estar aflito, sofrendo
e gravemente enfermo, [com uma enfermidade que requer o tratamento com água
fria, o que é proibido pelos votos que ele tomou], e pode ser que ele recuse a
água fria, [embora mentalmente a deseje], e use apenas a água quente, [que é
permitida, mantendo assim os seus votos através do corpo e da linguagem]. Porque
ele não obtém a água fria pode ser que ele morra. Agora, chefe de família, em
que lugar o Nigantha Nataputta descreveria a ocorrência do renascimento dele?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, existem devas chamados ‘atados pela mente’; ele renasceria dentre eles.
Por que isso? Porque ao morrer ele ainda estava preso, [pelo apego], à mente.” [<a
href="#N6">6</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Chefe de família, chefe de família, preste atenção à sua reposta! O que
você disse antes não está de acordo com o que disse depois, nem aquilo que você
disse depois está de acordo com o que disse antes. No entanto você fez esta
afirmação: ‘Eu<span style='mso-spacerun:yes'>  </span>debaterei com base na
verdade, venerável senhor, então conversemos sobre isso’.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, embora o Abençoado tenha dito isso, a vara corporal no entanto é a mais
repreensível para a realização de uma ação prejudicial, para a perpetração de
uma ação prejudicial, e não tanto a vara verbal e a vara mental.”[<a href="#N7">7</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R7></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>12. “O que você pensa, chefe de família? Aqui, algum Nigantha pode estar
contido com quatro controles -<span style='mso-spacerun:yes'>  </span>coibido por
todas as coibições, sujeitado por todas as coibições, purificado por todas as
coibições e demandado por todas as coibições [<a href="#N8">8</a>] <a name=R8></a>–
e no entanto ao caminhar para frente e regressar ele realiza a destruição de
muitas criaturas minúsculas. Que conseqüência o<span style='mso-spacerun:yes'> 
</span>Nigantha Nataputta descreve para ele?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, o Nigantha Nataputta não descreve aquilo que não é um ato da vontade
como sendo muito censurável.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas se
alguém tem volição, chefe de família?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então é
muito censurável, venerável senhor.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas sob
qual [das três varas] o Nigantha Nataputta descreve a volição, chefe de
família?”<o:p></o:p></span></p>

