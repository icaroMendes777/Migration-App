<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, terríveis são o ganho, a honraria e fama, amargos, vis,
obstruem a realização da insuperável segurança contra o cativeiro. Bhikkhus, eu
soube de uma certa pessoa, aqui, cuja mente compreendi com a minha própria mente:
‘Esse venerável seria incapaz de contar uma mentira deliberada para obter um
vaso de prata cheio de ouro em pó.’ No entanto, algum tempo mais tarde eu o vi,
com a mente tomada e obcecada pelo ganho, honraria e fama, contando uma
mentira deliberada. Tão terríveis, bhikkhus, são o ganho, a honraria e fama, amargos, vis, obstruem a realização da insuperável segurança contra o
cativeiro. Portanto, bhikkhus, vocês devem praticar da seguinte forma: ‘Nós
abandonaremos o ganho, a honraria e fama que já surgiram e não permitiremos
que o ganho, a honraria e fama que já surgiram persistam obcecando as
nossas mentes.’ Assim vocês deveriam praticar.”<b style='mso-bidi-font-weight:
