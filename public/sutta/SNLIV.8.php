<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
quando a concentração através da atenção plena na respiração é desenvolvida e cultivada,
traz grandes frutos e grandes benefícios. E como, bhikkhus, a concentração
através da atenção plena na respiração é desenvolvida e cultivada para que
traga grandes frutos e grandes benefícios?<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui um
bhikkhu, dirigindo-se à floresta ou à sombra de uma árvore ou a um local
isolado; senta-se com as pernas cruzadas, mantém o corpo ereto e estabelecendo
a plena atenção à sua frente, ele inspira com atenção plena justa, ele expira com atenção plena justa ... Ele treina assim: ‘Eu inspiro contemplando a
renúncia’; ele treina dessa forma: ‘Eu expiro contemplando a renúncia.’ [<a
href="#N1">1</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Bhikkhus, assim é como a concentração através da atenção plena na
respiração é desenvolvida e cultivada para que traga grandes frutos e grandes benefícios.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu também,
bhikkhus, antes da minha iluminação, quando eu ainda era apenas um Bodisatva
não iluminado, em geral permanecia nessa permanência. Enquanto eu em geral
permanecia nessa permanência, nem o meu corpo e tampouco os meus olhos ficavam
fatigados e a minha mente, através do desapego estava libertada das impurezas.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
bhikkhus, se um bhikkhu desejar: ‘Que nem o meu corpo e nem os meus olhos
fiquem fatigados e a minha mente, através do desapego esteja libertada das
impurezas,’ para essa mesma concentração através da atenção plena na respiração
é que ele deveria dar atenção. [<a href="#N2">2</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Portanto, bhikkhus, se um bhikkhu desejar: ‘Que as memórias e intenções
conectadas com a vida em família sejam abandonadas por mim,’ para essa mesma
concentração através da atenção plena na respiração é que ele deveria dar
atenção. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
bhikkhus, se um bhikkhu desejar: [<a href="#N3">3</a>] <a name=R3></a>‘Que eu
permaneça percebendo o repulsivo no não repulsivo,’ para essa mesma
concentração através da atenção plena na respiração é que ele<span
style="mso-spacerun: yes">  </span>deve dar atenção. Se um bhikkhu desejar:
‘Que eu permaneça percebendo o não repulsivo no repulsivo,’ para essa mesma
concentração através da atenção plena na respiração é que ele deveria dar atenção.
Se um bhikkhu desejar: ‘Que eu permaneça percebendo o repulsivo no não
repulsivo e no repulsivo,’ para essa mesma concentração através da atenção
plena na respiração é que ele deveria dar atenção. Se um bhikkhu desejar: ‘Que
eu permaneça percebendo o não repulsivo no repulsivo no não repulsivo,’ para
essa mesma concentração através da atenção plena na respiração é que ele
deveria dar atenção. Se um bhikkhu desejar: ‘Evitando ambos, o não repulsivo e
o repulsivo, que eu permaneça equânime, com atenção plena e plena consciência,’
para essa mesma concentração através da atenção plena na respiração é que ele
deveria dar atenção. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
bhikkhus, se um bhikkhu desejar: ‘Que eu, afastado dos prazeres sensuais,
afastado das qualidades não hábeis, entre e permaneça no primeiro jhana, que é
caracterizado pelo pensamento aplicado e sustentado, com o êxtase e felicidade
nascidos do afastamento,’ para essa mesma concentração através da atenção plena
na respiração é que ele deveria dar atenção.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
bhikkhus, se um bhikkhu desejar: ‘Que eu, abandonando o pensamento aplicado e
sustentado, entre e permaneça no segundo jhana, que é caracterizado pela segurança interna e perfeita 
unicidade da mente, sem o pensamento aplicado e sustentado, com o êxtase e felicidade nascidos da concentração,’ 
para essa mesma concentração
através da atenção plena na respiração é que ele deveria dar atenção.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
bhikkhus, se um bhikkhu desejar: ‘Que eu, abandonando o êxtase,
entre e permaneça no terceiro jhana que é caracterizado pela felicidade sem o êxtase, acompanhada pela atenção plena, 
plena consciência e equanimidade, acerca do qual os nobres declaram: "Ele permanece numa estada feliz, equânime e 
plenamente atento,"' para essa mesma concentração através da atenção plena na
respiração é que ele deveria dar atenção.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
bhikkhus, se um bhikkhu desejar: ‘Que eu, com o completo desaparecimento da felicidade, entre e permaneça no quarto jhana, 
que possui nem felicidade nem sofrimento, com a atenção plena e a equanimidade purificadas,’ para essa mesma concentração
através da atenção plena na respiração é que ele deveria dar atenção.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto, bhikkhus,
se um bhikkhu desejar: ‘Que eu, com a completa superação das percepções da forma, com o desaparecimento das percepções do contato sensorial, sem dar
atenção às percepções da diversidade, consciente de que o “espaço é infinito,”
entre e permaneça na base do espaço infinito,’ para essa mesma concentração
através da atenção plena na respiração é que ele deveria dar atenção.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
bhikkhus, se um bhikkhu desejar: ‘Que eu, com a completa superação da base do
espaço infinito, consciente de que a “consciência é infinita,” entre e
permaneça na base da consciência infinita,’ para essa mesma concentração
através da atenção plena na respiração é que ele deveria dar atenção.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
bhikkhus, se um bhikkhu desejar: ‘Que eu, com a completa superação da base da
consciência infinita, consciente de que “não há nada,” entre e permaneça na
base do nada,’ para essa mesma concentração através da atenção plena na
respiração é que ele deveria dar atenção.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
bhikkhus, se um bhikkhu desejar: ‘Que eu, com a completa superação da base do
nada, entre e permaneça na base da nem percepção, nem não percepção,’ para essa
mesma concentração através da atenção plena na respiração é que ele deveria dar
atenção.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
bhikkhus, se um bhikkhu desejar: ‘Que eu, com a completa superação da base da
nem percepção, nem não percepção, entre e permaneça na cessação da percepção e
sensação,’ para essa mesma concentração através da atenção plena na respiração
é que ele deveria dar atenção.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando,
bhikkhus, a concentração através da atenção plena na respiração é desenvolvida
e cultivada desse modo, [<a href="#N4">4</a>] <a name=R4></a>se ele sentir uma
sensação prazerosa, ele compreende: ‘Ela é impermanente’; ele compreende: ‘Não
é para se apegar’; ele compreende: ‘Não é para se deleitar.’ Se ele sentir uma
sensação dolorosa, ele compreende: ‘Ela é impermanente’; ele compreende: ‘Não é
para se apegar’; ele compreende: ‘Não é para se deleitar.’ Se ele sentir uma
sensação nem prazerosa, nem dolorosa, ele compreende: ‘Ela é impermanente’; ele
compreende: ‘Não é para se apegar’; ele compreende: ‘Não é para se deleitar.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se ele
sentir uma sensação prazerosa, ele a sente desapegado; se ele sentir uma
sensação dolorosa, ele a sente desapegado; Se ele sentir uma sensação nem prazerosa,
nem dolorosa, ele a sente desapegado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando ele
sente uma sensação que dá um fim ao corpo, ele compreende: ‘Eu sinto uma
sensação que dá um fim ao corpo.’ Quando ele sente uma sensação que dá um fim à
vida, ele compreende: ‘Eu sinto uma sensação que dá um fim à vida.’ Ele
compreende: ‘Com a dissolução do corpo, depois da morte, tudo que é sentido sem
deleitação, irá esfriar aqui mesmo, apenas resíduos corporais restarão.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Assim
como, bhikkhus, uma lamparina queima na dependência do óleo e do pavio, com a
exaustão do óleo e do pavio a chama se extingue por falta de combustível, da
mesma maneira, bhikkhus, quando um bhikkhu sente uma sensação que dá um fim ao
corpo ... que dá um fim à vida ... ele compreende: ‘Eu sinto uma sensação que
dá um fim à vida.’ Ele compreende: ‘Com a dissolução do corpo, depois da morte,
tudo que é sentido sem deleitação, irá esfriar aqui mesmo, apenas resíduos
corporais restarão.’”<o:p></o:p></span></p>




