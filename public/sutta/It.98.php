<p class=Normal>Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: "Bhikkhus, há dois tipos de generosidade. Quais dois? A generosidade com bens materiais e a generosidade com o Dhamma. Esses são os dois tipos de generosidade. Desses dois tipos, a generosidade com o Dhamma é superior."</p>

<p class=Normal>"Bhikkhus, há dois modos de compartir. Quais dois? Compartir bens materiais e compartir o Dhamma. Esses são os dois modos de compartir. Desses dois modos, compartir o Dhamma é superior."</p>

<p class=Normal>"Bhikkhus, há dois tipos de assistência. Quais dois? Assistência com bens materiais e assistência com o Dhamma. Esses são os tipos de assistência. Desses dois tipos, a assistência com o Dhamma é superior."</p>

<p class=Normal>Quando se diz que a generosidade <br>
é suprema e insuperável, <br>
e tendo o próprio Abençoado exaltado a generosidade: <br>
quem, com conhecimento e sabedoria, <br>
confiante nesse supremo campo de mérito, <br>
não daria no momento apropriado?</p>

<p class=Normal>Ambos, para aqueles que proclamam<br>
e para aqueles que ouvem<br>
com convicção a mensagem do Iluminado: <br>
purificam o benefício supremo -<br>
aqueles que atendem<br>
à mensagem do Abençoado. </p>

