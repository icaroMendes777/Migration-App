<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião o Abençoado estava em Rajagaha, no Bambual, no Santuário
dos Esquilos. Então, ao anoitecer, o venerável Sariputta levantou da meditação
e foi até o Abençoado. Depois de cumprimentá-lo, ele sentou a um lado. O
Abençoado disse:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.
“Sariputta, as suas faculdades estão serenas. A sua complexão está pura e
brilhante. Em qual permanência você freqüentemente permanece agora, Sariputta?”
<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Agora,
venerável senhor, eu permaneço freqüentemente no vazio.” [<a href="#N1">1</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Muito bem, Sariputta! Agora, de fato, você permanece freqüentemente na
permanência de um grande homem. Pois essa é a permanência de um grande homem,
isto é, o vazio. [<a href="#N2">2</a>] <o:p></o:p></span></p>



<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>3. “Portanto, Sariputta, se um bhikkhu desejar: ‘Que eu agora permaneça
freqüentemente no vazio,’ ele deveria considerar o seguinte: ‘No caminho que
percorri até o vilarejo para esmolar alimentos, ou no lugar em que perambulei
esmolando alimentos, ou no caminho de volta da esmola de alimentos, houve na
minha mente algum desejo, cobiça, raiva, delusão ou aversão com relação a
formas percebidas pelo olho?’ [<a href="#N3">3</a>] <a name=R3></a>Se, ao
revisar deste modo, ele compreende que: ‘No caminho que percorri até o vilarejo
para esmolar alimentos, ou no lugar em que perambulei esmolando alimentos, ou
no caminho de volta da esmola de alimentos, houve na minha mente desejo,
cobiça, raiva, delusão ou aversão com relação a formas percebidas pelo olho,’
então, ele deve fazer o esforço para abandonar esses estados ruins e
prejudiciais. Mas, se ao revisar ele compreende que: ‘No caminho que percorri
até o vilarejo para esmolar alimentos, ou no lugar em que perambulei esmolando
alimentos, ou no caminho de volta da esmola de alimentos, não houve na minha
mente nenhum desejo, cobiça, raiva, delusão ou aversão com relação a formas
percebidas pelo olho,’ então, ele pode permanecer feliz e contente, treinando
dia e noite nos estados benéficos. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4-8. “Outra
vez, Sariputta, um bhikkhu deveria considerar o seguinte: ‘No caminho que
percorri até o vilarejo para esmolar alimentos, ou no lugar em que perambulei
esmolando alimentos, ou no caminho de volta da esmola de alimentos, houve na
minha mente algum desejo, cobiça, raiva, delusão ou aversão com relação a sons
percebidos pelo ouvido? ... com relação a aromas percebidos pelo nariz? ... com
relação a sabores percebidos pela língua? ... com relação a tangíveis
percebidos pelo corpo? ... com relação a objetos mentais percebidos pela
mente?’ Se, ao revisar deste modo, ele compreende que: ‘No caminho que percorri
até o vilarejo para esmolar alimentos, ou no lugar em que perambulei esmolando
alimentos, ou no caminho de volta da esmola de alimentos, houve na minha mente
desejo, cobiça, raiva, delusão ou aversão com relação a objetos mentais
percebidos pela mente,’ então, ele deve fazer o esforço para abandonar esses
estados ruins e prejudiciais. Mas, se ao revisar ele compreende que: ‘No
caminho que percorri até o vilarejo para esmolar alimentos, ... não houve na
minha mente nenhum desejo, cobiça, raiva, delusão ou aversão com relação a
objetos mentais percebidos pela mente,’ então, ele pode permanecer feliz e
contente, treinando dia e noite nos estados benéficos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. “Outra
vez, Sariputta, um bhikkhu deveria considerar o seguinte: ‘Os cinco elementos
do prazer sensual foram abandonados por mim?’ [<a href="#N4">4</a>] <a name=R4></a>Se,
ao revisar deste modo, ele compreende que: ‘Os cinco elementos do prazer
sensual não foram abandonados por mim,’ então, ele deve fazer o esforço para abandonar
esses cinco elementos do prazer sensual. Mas, se ao revisar ele compreende que:
‘Os cinco elementos do prazer sensual foram abandonados por mim,’ então, ele
pode permanecer feliz e contente, treinando dia e noite nos estados benéficos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. “Outra
vez, Sariputta, um bhikkhu deveria considerar o seguinte: ‘Os cinco obstáculos
foram abandonados por mim?’ Se, ao revisar deste modo, ele compreende que: ‘Os
cinco obstáculos não foram abandonados por mim,’ então, ele deve fazer o
esforço para abandonar esses cinco obstáculos. Mas, se ao revisar ele
compreende que: ‘Os cinco obstáculos foram abandonados por mim,’ então ele pode
permanecer feliz e contente, treinando dia e noite nos estados benéficos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. “Outra vez,
Sariputta, um bhikkhu deveria considerar o seguinte: ‘Os cinco agregados
influenciados pelo apego foram compreendidos completamente por mim?’<span
style="mso-spacerun: yes">  </span>Se, ao revisar deste modo, ele compreende
que: ‘Os cinco agregados influenciados pelo apego não foram compreendidos
completamente por mim,’ então, ele deve fazer o esforço para compreender
completamente esses cinco agregados influenciados pelo apego. Mas, se ao
revisar ele compreende que: ‘Os cinco agregados influenciados pelo apego foram
compreendidos completamente por mim,’ então ele pode permanecer feliz e
contente, treinando dia e noite nos estados benéficos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. “Outra
vez, Sariputta, um bhikkhu deveria considerar o seguinte: ‘Os quatro
fundamentos da atenção plena foram desenvolvidos por mim?’<span
style="mso-spacerun: yes">  </span>Se, ao revisar deste modo, ele compreende
que: ‘Os quatro fundamentos da atenção plena não foram desenvolvidos por mim,’
então, ele deve fazer o esforço para desenvolver esses quatro fundamentos da
atenção plena. Mas, se ao revisar ele compreende que: ‘Os quatro fundamentos da
atenção plena foram desenvolvidos por mim,’ então, ele pode permanecer feliz e
contente, treinando dia e noite nos estados benéficos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13-19.
“Outra vez, Sariputta, um bhikkhu deveria considerar o seguinte: ‘Os quatro
tipos de esforço correto foram desenvolvidos por mim? ... As quatro bases para
o poder espiritual foram desenvolvidas por mim? .... As cinco faculdades foram
desenvolvidas por mim? ... Os cinco poderes foram desenvolvidos por mim? ... Os
sete fatores da iluminação foram desenvolvidos por mim? ... O Nobre Caminho
Óctuplo foi desenvolvido por mim? ... A tranqüilidade e o insight foram
desenvolvidos por mim?’ Se, ao revisar deste modo, ele compreende que: ‘A
tranqüilidade e o insight não foram desenvolvidos por mim,’ então, ele deve fazer
o esforço para desenvolvê-los. Mas, se ao revisar ele compreende que: ‘A
tranqüilidade e o insight foram desenvolvidos por mim,’ então, ele pode
permanecer feliz e contente, treinando dia e noite nos estados benéficos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>20. “Outra
vez, Sariputta, um bhikkhu deveria considerar o seguinte: ‘O conhecimento
verdadeiro e a libertação foram realizados por mim?’ Se, ao revisar deste modo,
ele compreende que: ‘O conhecimento verdadeiro e a libertação não foram
realizados por mim,’ então ele deve fazer o esforço para realizar o
conhecimento verdadeiro e a libertação. Mas, se ao revisar ele compreende que:
‘O conhecimento verdadeiro e a libertação foram realizados por mim,’ então, ele
pode permanecer feliz e contente, treinando dia e noite nos estados benéficos.
[<a href="#N5">5</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R5></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>21. “Sariputta, todos os contemplativos e brâmanes do passado, que
purificaram as suas esmolas, assim o fizeram revisando repetidamente dessa
maneira. Todos os contemplativos e brâmanes do futuro, que purificarem as suas
esmolas, assim o farão revisando repetidamente dessa maneira. Todos os
contemplativos e brâmanes do presente, que purificam as suas esmolas, assim o
fazem revisando repetidamente dessa maneira. Portanto, Sariputta, você deve
treinar deste modo: ‘Nós purificaremos as nossas esmolas revisando
repetidamente dessa maneira.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o
que disse o Abençoado. O venerável Sariputta ficou satisfeito e contente com as
palavras do Abençoado.<o:p></o:p></span></p>


