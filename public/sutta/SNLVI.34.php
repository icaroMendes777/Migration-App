<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
se as roupas ou a cabeça de alguém estiver em chamas, o que ele deve fazer a
respeito disso?”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, se as roupas ou a cabeça de alguém estiver em chamas, para extinguir as
chamas das roupas ou da cabeça ele deveria despertar uma aspiração
extraordinária, fazer um esforço extraordinário, despertar o ardor e o
entusiasmo, ser persistente, praticar a atenção plena e a plena consciência.” [<a
href="#N1">1</a>]<o:p></o:p></span></p>


<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Bhikkhus, alguém poderá observar com equanimidade as próprias roupas ou
a cabeça em chamas, não lhes dando atenção, mas desde que ele não tenha <span
style='mso-spacerun:yes'> </span>penetrado as Quatro Nobres Verdades como na
verdade elas são, para penetrá-las ele deve despertar uma aspiração
extraordinária, fazer um esforço extraordinário, despertar o ardor e o
entusiasmo, ser persistente, praticar a atenção plena e a plena consciência.
Quais quatro? A nobre verdade do sofrimento, a nobre verdade da origem do
sofrimento, a nobre verdade da cessação do sofrimento, a nobre verdade do
caminho que conduz à cessação do sofrimento.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
bhikkhus, um esforço deve ser feito para compreender: ‘Isto é sofrimento.’ Um
esforço deve ser feito para compreender: ‘Esta é a origem do sofrimento.’ Um
esforço deve ser feito para compreender: ‘Esta é a cessação do sofrimento.’ Um
esforço deve ser feito para compreender: ‘Este é o caminho que conduz à
cessação do sofrimento.’”<o:p></o:p></span></p>

