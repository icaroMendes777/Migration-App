<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, aquilo que alguém intenciona, aquilo que alguém planeja e aquilo
que alguém tenha como tendência subjacente: isto se torna uma base para a manutenção da
consciência. Quando há uma base, haverá um suporte para o estabelecimento da
consciência. Quando a consciência se estabelece e cresce, há a produção de um
renovado ser/existir futuro. Quando há a produção de um renovado ser/existir
futuro, o futuro nascimento e morte, lamentação, dor, angústia e desespero
surgem. Essa é a origem de toda essa massa de sofrimento. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se,
bhikkhus, alguém não intenciona, alguém não planeja, mas ainda tem como tendência subjacente,
isto se torna uma base para a manutenção da consciência. Quando há uma base,
haverá um suporte para o estabelecimento da consciência ...<span
style="mso-spacerun: yes">  </span>Essa é a origem de toda essa massa de
sofrimento.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas,
bhikkhus, quando alguém não intenciona, alguém não planeja e não tem como tendência subjacente, nenhuma base existe para a manutenção da consciência. Quando não há uma
base, não haverá suporte para o estabelecimento da consciência. Quando a
consciência não se estabelece e não cresce, não há a produção de um renovado
ser/existir futuro. Quando não há a produção de um renovado ser/existir futuro,
o futuro nascimento e morte, lamentação, dor, angústia e desespero cessam. Essa
é a cessação de toda essa massa de sofrimento<o:p></o:p></span></p>


