<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião o Abençoado estava em Savathi, no Bosque de Jeta, no
Parque de Anathapindika. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, ao
anoitecer, o Venerável Maha Kotthita levantou-se da meditação, foi até o
Venerável Sariputta e ambos se cumprimentaram. [<a href="#N1">1</a>] <a
name=R1></a>Depois que a conversa cortês e amigável havia terminado, ele sentou
a um lado e disse para o Venerável Sariputta:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(Sabedoria)</b>
<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.
&quot;'Alguém que não é sábio, alguém que não é sábio' se diz, amigo. Com
referência a que se diz, 'alguém que não é sábio'?&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;'A
pessoa não compreende, a pessoa não compreende', amigo; é por isso que se diz ,
'alguém que não é sábio'. E o que a pessoa não compreende? A pessoa não
compreende: 'Isto é sofrimento'; a pessoa não compreende: 'Esta é a origem do
sofrimento'; a pessoa não compreende: 'Esta é a cessação do sofrimento'; a
pessoa não compreende: 'Este é o caminho que conduz à cessação do sofrimento'.
'A pessoa não compreende, a pessoa não compreende', amigo; é por isso que se
diz , 'alguém que não é sábio'&quot;.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Dizendo,
&quot;Muito bem, amigo&quot;, o Venerável Maha Kotthita ficou satisfeito e contente
com as palavras do Venerável Sariputta. Em seguida ele fez outra pergunta: <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3.
&quot;'Alguém que é sábio, alguém que é sábio', se diz, amigo. Com referência a
que se diz, 'alguém que é sábio'?&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;'A
pessoa compreende, a pessoa compreende', amigo; é por isso que se diz , 'alguém
que é sábio'. E o que a pessoa compreende? A pessoa compreende: 'Isto é
sofrimento'; a pessoa compreende: 'Esta é a origem do sofrimento'; a pessoa
compreende: 'Esta é a cessação do sofrimento'; a pessoa compreende:
'Este é o caminho que conduz à cessação do sofrimento'. 'A pessoa compreende, a
pessoa compreende', amigo; é por isso que se diz , 'alguém que é sábio'&quot;.
[<a href="#N2">2</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'><b>(Consciência)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4.
&quot;'Consciência, consciência,' se diz, amigo. Com referência a que se diz,
'consciência'?&quot; <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;’É conscientizar,
é conscientizar,’ amigo; é por isso que se diz, 'consciência'. [<a href="#N3">3</a>]
<a name=R3></a>E o que é conscientizado? É conscientizado: '(Isto é)
prazeroso'; é conscientizado: '(Isto é) doloroso'; é conscientizado: '(Isto é)
nem prazeroso, nem doloroso'. 'É conscientizar, é conscientizar,’ amigo; é por
isso que se diz, 'consciência'. [<a href="#N4">4</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>5. &quot;Sabedoria e consciência, amigo - esses estados são associados
ou dissociados? É possível separar cada um desses estados um do outro de modo a
descrever a diferença entre eles?' <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Sabedoria
e consciência, amigo - esses estados são associados, não dissociados, e é
impossível separar cada um desses estados um do outro de modo a descrever a
diferença entre eles. Pois aquilo que a pessoa compreende com sabedoria, isso
ela conscientiza, e aquilo que ela conscientiza, isso ela compreende com
sabedoria. É por isso que esses estados são associados, não dissociados e é
impossível separar cada um desses estados do outro de modo a descrever a
diferença entre eles&quot;. [<a href="#N5">5</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R5></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>6. “Qual é a diferença, amigo, entre a sabedoria e a consciência, esses
estados que são associados, não dissociados?&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;A
diferença, amigo, entre a sabedoria e a consciência, estados esses que são
associados, não dissociados, é esta: a sabedoria deve ser desenvolvida, a
consciência deve ser plenamente compreendida&quot;. [<a href="#N6">6</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'><b>(Sensação)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7.
&quot;'Sensação, sensação', se diz, amigo. Com referência a que se diz,
'sensação'?&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;É o
sentir, é o sentir', amigo; é por isso que se diz, 'sensação'. E o que é
sentido? É sentido prazer, é sentido dor, é sentido nem prazer nem dor. 'É o
sentir, é o sentir', amigo; é por isso que se diz, 'sensação'&quot;. [<a
href="#N7">7</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R7></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'><b>(Percepção)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8.
&quot;'Percepção, percepção', se diz, amigo. Com referência a que se diz,
'percepção'?&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;É o
perceber, é o perceber', amigo; é por isso que se diz, 'percepção'. E o que é
percebido? É percebido o azul, é percebido o amarelo, é percebido o vermelho e
é percebido o branco. 'É o perceber, é o perceber', amigo; é por isso que se
diz, 'percepção'&quot;. [<a href="#N8">8</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R8></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>9. &quot; Sensação, percepção e consciência, amigo - esses estados são
associados ou dissociados? É possível separar cada um desses estados um do
outro de modo a descrever a diferença entre eles?' <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Sensação,
percepção e consciência, amigo - esses estados são associados, não dissociados,
e é impossível separar cada um desses estados um do outro de modo a descrever a
diferença entre eles. Pois aquilo que a pessoa sente, isso ela percebe; e
aquilo que ela percebe, isso ela conscientiza. É por isso que esses estados são
associados, não dissociados e é impossível separar cada um desses estados um do
outro de modo a descrever a diferença entre eles&quot;. [<a href="#N9">9</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R9></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'><b>(Que só pode ser conhecido pela mente)</b><o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. “Amigo,
o que pode ser conhecido pela consciência na mente purificada e libertada das
cinco faculdades?&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Amigo,
através da consciência na mente purificada e libertada das cinco faculdades a
base do espaço infinito pode ser conhecida assim: 'O espaço é infinito'; a base
da consciência infinita pode ser conhecida assim: 'A consciência é infinita'; e
a base do nada pode ser conhecida assim: 'Não há nada'&quot;. [<a href="#N10">10</a>]</span><span
lang=PT-BR style='font-size:11.0pt;mso-bidi-font-size:12.0pt;mso-ansi-language:
PT-BR'><o:p></o:p></span></p>



<p class=Normal><a name=R10></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>11. “Amigo, com que a pessoa compreende um estado que pode ser compreendido?&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Amigo,
a pessoa compreende um estado que pode ser compreendido com o olho da
sabedoria&quot;. [<a href="#N11">11</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R11></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>12. “Amigo, qual é o propósito da sabedoria?&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;O
propósito da sabedoria, amigo, é o conhecimento direto, o seu propósito é a
completa compreensão, o seu propósito é o abandono&quot;. [<a href="#N12">12</a>]<o:p></o:p></span></p>


<p class=Normal><a name=R12></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'><b>(Entendimento Correto)</b><o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. “Amigo,
quantas condições existem para o surgimento do entendimento correto?&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Amigo,
existem duas condições para o surgimento do entendimento correto: a voz de uma
outra pessoa e a atenção com sabedoria. Essas são as duas condições para o
surgimento do entendimento correto&quot;. [<a href="#N13">13</a>]</span><span
lang=PT-BR style='font-size:11.0pt;mso-bidi-font-size:12.0pt;mso-ansi-language:
PT-BR'><o:p></o:p></span></p>



<p class=Normal><a name=R13></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>14. “Amigo, quantos fatores suportam o entendimento correto quando este tem
a libertação da mente como o seu fruto, a libertação da mente como o seu fruto
e benefício, quando tem a libertação através da sabedoria como o seu fruto, a
libertação através daa sabedoria como seu fruto e benefício?&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Amigo,
o entendimento correto é suportado por cinco fatores quando tem a libertação da
mente como o seu fruto, a libertação da mente como o seu fruto e benefício,
quando tem a libertação através da sabedoria como o seu fruto, a libertação através da
sabedoria como seu fruto e benefício. Nesse caso, amigo, o entendimento correto
é suportado pela virtude, aprendizado, discussão, tranquilidade e insight. O
entendimento correto suportado por esses cinco fatores tem a libertação da
mente como o seu fruto, a libertação da mente como o seu fruto e benefício,
quando tem a libertação através da sabedoria como o seu fruto, a libertação através da
sabedoria como seu fruto e benefício.&quot; [<a href="#N14">14</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R14></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'><b>(Ser/existir)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15.
&quot;Amigo, quantos tipos de seres existem?&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Existem
três tipos de seres, amigo: seres do reino da esfera sensual, seres do reino da
matéria sutil e seres do reino imaterial.&quot; <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16.
&quot;Amigo, como é gerada a renovação dos seres no futuro?&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Amigo,
a renovação dos seres no futuro é gerada através do deleite com isto e com
aquilo por parte dos seres que são atrapalhados pela ignorância e agrilhoados pelo
desejo.&quot; [<a href="#N15">15</a>] <o:p></o:p></span></p>



<p class=Normal><a name=R15></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>17. &quot;Amigo, como a renovação dos seres no futuro não é
gerada?&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Amigo,
com a dissolução da ignorância, com o surgimento do verdadeiro conhecimento e
com a cessação do desejo, a renovação dos seres no futuro não é gerada.&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(O Primeiro
Jhana)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18.
&quot;Amigo, o que é o primeiro jhana?&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Neste
caso, amigo, um bhikkhu afastado dos prazeres sensuais, afastado das qualidades não hábeis, 
entra e permanece no primeiro jhana, que é caracterizado pelo pensamento aplicado e sustentado, 
com o êxtase e felicidade nascidos do afastamento. A isto se denomina o primeiro jhana.&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>19.
&quot;Amigo, quantos fatores possui o primeiro jhana?&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Amigo,
o primeiro jhana possui cinco fatores. Quando um bhikkhu entra no primeiro
jhana, ocorrem o pensamento aplicado, o pensamento sustentado, o êxtase, a
felicidade e a unificação da mente. Esses são os cinco fatores do primeiro
jhana.&quot; <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>20. 'Amigo,
quantos fatores são abandonados no primeiro jhana e quantos fatores são
possuídos?&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Amigo,
no primeiro jhana cinco fatores são abandonados e cinco fatores são possuídos.
Quando um bhikkhu entra<span style="mso-spacerun: yes">  </span>no primeiro
jhana, o desejo sensual é abandonado, a má vontade é abandonada, o torpor e a
preguiça são abandonados, a inquietação e a ansiedade são abandonadas, e a dúvida
é abandonada; e ocorrem o pensamento aplicado, o pensamento sustentado, o
êxtase, a felicidade e a unificação da mente. Assim é como no primeiro jhana
cinco fatores são abandonados e cinco fatores são possuídos.&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(As Cinco
Faculdades)</b><o:p></o:p></span></p>



<p class=Normal><a name=T21></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>21. &quot;Amigo, essas cinco faculdades possuem cada uma um campo
separado, um domínio separado e não experimentam o campo e o domínio das
demais, isto é, a faculdade do olho, a faculdade do ouvido, a faculdade do
nariz, a faculdade da língua e a faculdade do corpo. Agora, essas cinco
faculdades, cada uma possuindo um campo separado, um domínio separado e não
experimentando o campo e o domínio das demais, a que elas recorrem, o que é que
experimenta os seus campos e os seus domínios?&quot; [<a href="#N16">16</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R16></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>&quot;Amigo, estas cinco faculdades possuem cada uma um campo separado,
um domínio separado e não experimentam o campo e o domínio das demais, isto é,
a faculdade do olho, a faculdade do ouvido, a faculdade do nariz, a faculdade da
língua e a faculdade do corpo. Agora, essas cinco faculdades, cada uma
possuindo um campo separado, um domínio separado e não experimentando o campo e
o domínio das demais, recorrem à mente e a mente experimenta os seus campos e
os seus domínios.&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>22.
&quot;Amigo, quanto a essas cinco faculdades - isto é, a faculdade do olho, a
faculdade do ouvido, a faculdade do nariz, a faculdade da língua e a faculdade
do corpo - do que dependem essas cinco faculdades?&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Amigo,
quanto a essas cinco faculdades - isto é, a faculdade do olho, a faculdade do
ouvido, a faculdade do nariz, a faculdade da língua e a faculdade do corpo -
essas cinco faculdades dependem da vitalidade.&quot; [<a href="#N17">17</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R17></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>&quot; Amigo, e do que depende a vitalidade?&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;A vitalidade
depende do calor.&quot; [<a href="#N18">18</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R18></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>&quot; Amigo, e do que depende o calor?&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;O
calor depende da vitalidade.&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Agora
mesmo, amigo, entendemos que o Venerável Sariputta havia dito : 'A vitalidade
depende do calor'; e agora entendemos que ele disse: 'O calor depende da
vitalidade.' Como deve ser interpretado o significado desses enunciados?&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Nesse
caso, amigo, eu explicarei com um símile pois alguns sábios compreendem o
significado de um enunciado através de um símile. Como quando uma lâmpada de
óleo está queimando, o seu brilho é visto na dependência da sua chama e a sua
chama é vista na dependência do seu brilho; da mesma forma, a vitalidade
depende do calor e o calor depende da vitalidade.&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(Formações
Vitais)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>23.
&quot;Amigo, as formações vitais são sensações ou as formações vitais são uma
coisa e as sensações outra?&quot;<o:p></o:p></span></p>



<p class=Normal style='tab-stops:279.0pt'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>&quot;As formações vitais, amigo, não são sensações. [<a href="#N19">19</a>]
<a name=R19></a>Se as formações vitais fossem sensações, então quando um
bhikkhu entrasse na cessação da percepção e da sensação, ele não seria visto
emergir dela. Como as formações vitais são uma coisa e as sensações outra,
quando um bhikkhu entra na cessação da percepção e da sensação, ele pode ser
