<p class=Normal> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: "Possuindo duas coisas, um bhikkhu permanece com o sofrimento nesta vida - aflito, perturbado, angustiado - e com a dissolução do corpo, após a morte um destino ruim pode ser esperado. Quais duas?  Não guardar as portas dos meios dos sentidos, e não ter moderação ao comer. Possuindo essas duas coisas, um bhikkhu permanece com o sofrimento nesta vida - aflito, perturbado, angustiado - e com a dissolução do corpo, após a morte um destino ruim pode ser esperado."</p>

<p class=Normal style='margin-left:.75in'>Olho, ouvido, nariz, <br>
língua, corpo, e mente,<br>
um bhikkhu que não guarda essas portas, <br>
sem conter os sentidos, <br>
sem moderação ao comer, <br>
ele experimenta o sofrimento: <br>
sofrimento no corpo, <br>
sofrimento na mente. <br>
Queimação  no corpo <br>
queimação na mente. <br>
Quer seja dia ou noite, <br>
ele permanece com o sofrimento.</p>


