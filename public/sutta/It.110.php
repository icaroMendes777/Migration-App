<p class=Normal> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: "Bhikkhus, se enquanto estiver caminhando um pensamento de desejo sensual, ou um pensamento de má vontade, ou um pensamento de crueldade, surge num bhikkhu e se ele tolerá-lo e não rejeitá-lo, dissipá-lo e se livrar dele, dando um fim nele, esse bhikkhu - que dessa forma está carente de ardência e não teme cometer transgressões - é chamado de constantemente preguiçoso e indolente. Se enquanto estiver em pé ... se enquanto estiver sentado ... se enquanto estiver deitado um pensamento de desejo sensual, ou um pensamento de má vontade, ou um pensamento de crueldade, surge num bhikkhu e se ele tolerá-lo e não rejeitá-lo, dissipá-lo e se livrar dele, dando um fim nele, esse bhikkhu - que dessa forma está carente de ardência e não teme cometer transgressões - é chamado de constantemente preguiçoso e indolente.</p>

<p class=Normal> "Mas se enquanto estive caminhando ... em pé ... sentado ... deitado um pensamento de desejo sensual, ou um pensamento de má vontade, ou um pensamento de crueldade, surge num bhikkhu e se ele não tolerá-lo mas rejeitá-lo, dissipá-lo e se livrar dele, dando um fim nele, esse bhikkhu - que dessa forma está ardente e teme cometer transgressões - é chamado de constantemente energético e decidido."</p>

<p class=Normal style='margin-left:.75in'> Quer seja andando ou em pé, <br>
sentado ou deitado,<br>
quem pense esse tipo de pensamentos<br>
que são ruins e mundanos -<br>
segue o caminho errado, <br>
encantado pelas delusões. <br>
Um bhikkhu assim não realizará<br>
a suprema iluminação.</p>

<p class=Normal style='margin-left:.75in'>Quer seja andando ou em pé, <br>
sentado ou deitado,<br>
quem supera esse tipo de pensamentos, <br>
deliciando-se ao domar os pensamentos -<br>
um bhikkhu assim será capaz de realizar<br>
a suprema iluminação.</p>



