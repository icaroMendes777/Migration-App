<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, a forma é carvão em brasa, a sensação é carvão em brasa, a
percepção é carvão em brasa, as formações volitivas são carvão em brasa, a consciência é
carvão em brasa. Vendo dessa maneira, bhikkhus, o nobre discípulo bem instruído
se desencanta com a forma, desencanta com a sensação, desencanta com a
percepção, desencanta com as formações volitivas, desencanta com a consciência.
Desencantado ele se torna desapegado. Através do desapego a sua mente é
libertada. Quando ela está libertada surge o conhecimento: ‘Libertada.’ Ele
compreende que: ‘O nascimento foi destruído, a vida santa foi vivida, o que
deveria ser feito foi feito, não há mais vir a ser a nenhum estado.’”<b
