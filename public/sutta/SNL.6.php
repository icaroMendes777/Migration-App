<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
assim como<span style="mso-spacerun: yes">  </span>o rio Ganges decliva, tende
e se inclina na direção do oceano, um bhikkhu que desenvolve e cultiva os cinco
poderes também decliva, tende e se inclina na direção de Nibbana.[<a href="#N1">1</a>]<o:p></o:p></span></p>


<p class=Normal><a name=R2></a><a name=R1></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>“E como, bhikkhus, um bhikkhu desenvolve e
cultiva os cinco poderes para ele declivar, tender e se inclinar na direção de
Nibbana? Neste caso, bhikkhus, um bhikkhu desenvolve o poder da convicção, que
tem como base o afastamento, desapego e cessação, que amadurece no abandono ...
Ele desenvolve o poder da energia ... ele desenvolve o poder da atenção plena
... ele desenvolve o poder da concentração ... ele desenvolve o poder da sabedoria,
que tem como base o afastamento, desapego e cessação, que amadurece no abandono.
<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“É dessa
forma, bhikkhus, que um bhikkhu desenvolve e cultiva os cinco poderes para ele
declivar, tender e se inclinar na direção de Nibbana.”<o:p></o:p></span></p>


