<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim ouvi.
Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no Parque de
Anathapindika. Lá ele se dirigiu aos monges desta forma: “Bhikkhus” –
“Venerável Senhor,” eles responderam. O Abençoado disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(O TOLO)</b><o:p></o:p></span></p>



<p class=Normal><a name=T2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2. “Bhikkhus, existem essas três características de um tolo, sinais de
um tolo, atributos de um tolo. Quais três? Aqui, um tolo é aquele que pensa
pensamentos prejudiciais, diz palavras prejudiciais e pratica atos prejudiciais. Se um tolo não
fosse assim, como poderia o sábio reconhecê-lo desta forma: ‘Esta pessoa é uma
tola, uma pessoa falsa’? Pelo fato de um tolo ser aquele que pensa pensamentos
prejudiciais, diz palavras prejudiciais e pratica atos prejudiciais, o sábio o reconhece desta
forma: ‘Esta pessoa é uma tola, uma pessoa falsa.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. “Um tolo
sente dor e tristeza aqui e agora de três formas. Se um tolo estiver sentado
numa assembléia, numa rua, ou numa praça, e as pessoas ali estiverem
discutindo certos assuntos relevantes e pertinentes, então, se o tolo é alguém
que mata seres vivos, toma aquilo que não é dado, se comporta de forma
imprópria em relação aos prazeres sensuais, diz mentiras e se entrega ao vinho,
álcool e outros embriagantes, que causam a negligência, ele pensa: ‘Essas
pessoas estão discutindo certos assuntos relevantes e pertinentes; essas coisas
podem ser encontradas em mim, eu sou visto engajando-me nessas coisas.’ Esse é
o primeiro tipo de dor e tristeza que um tolo sente aqui e agora.<o:p></o:p></span></p>



<p class=Normal><a name=T4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>4. “Novamente, quando o acusado de um roubo é preso, um tolo vê os reis
aplicando nele diversos tipos de tortura: [<a href="#N1">1</a>] <a name=R1></a>açoitando
com chicotes, golpeando com vara, golpeando com clavas; as<span
style='mso-spacerun:yes'>  </span>mãos são cortadas, os pés são cortados, as
mãos e os pés são cortados; as orelhas são cortadas, o nariz é cortado, as
orelhas e o nariz são cortados; ele é sujeito ao ‘pote de mingau,’ ao ‘barbeado
com a concha polida,’ à ‘boca de Rahu,’ à ‘grinalda ardente,’ à ‘mão ardente,’
às ‘lâminas de capim,’ à ‘túnica de casca de árvore,’ ao ‘antílope,’ aos
‘ganchos de carne,’ às ‘moedas,’ à ‘conserva em desinfetante’ ao ‘pino que
gira,’ ao ‘colchão de palha enrolado’; ele é molhado com óleo fervente, atirado
para ser devorado pelos cães, empalado vivo em estaca, decapitado com uma
espada. Então o tolo pensa assim: ‘Devido a ações prejudiciais como essas, quando o
acusado de um roubo é preso, os reis aplicam nele diversos tipos de tortura:
eles o açoitam com chicotes ... e o decapitam com uma espada.<span
style='mso-spacerun:yes'>  </span>Essas coisas podem ser encontradas em mim, eu
sou visto engajando-me nessas coisas.’ Esse é o segundo tipo de dor e tristeza
que um tolo sente aqui e agora.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5.
“Novamente, quando um tolo está na sua cadeira, ou na sua cama, ou descansando no
chão, então as ações prejudiciais que ele cometeu no passado – as condutas impróprias
com o corpo, linguagem e mente – o cobrem, estendem sobre ele e o envelopam.
Tal como a sombra de uma grande montanha ao anoitecer cobre, se estende sobre a
terra<span style='mso-spacerun:yes'>  </span>e a envelopa, assim também, quando
um tolo está na sua cadeira, ou na sua cama, ou descansando no chão, então as ações
prejudiciais que ele cometeu no passado – as condutas impróprias com o corpo,
linguagem e mente – o cobrem, se estendem sobre ele e o envelopam. Então o tolo
pensa: ‘Eu não fiz o bem, eu não fiz aquilo que é benéfico, eu não fiz de mim
mesmo um abrigo contra a aflição. Eu fiz aquilo que é prejudicial, eu fiz aquilo que é
cruel, eu fiz aquilo que é mau. Quando falecer, eu irei para o destino daqueles
que não fizeram o bem ... que fizeram aquilo que é mau.’ Ele se entristece,
fica angustiado e lamenta, ele chora batendo no peito e fica perturbado. Esse é
o terceiro tipo de dor e tristeza que um tolo sente aqui e agora.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. “Um tolo
que se entregou à conduta imprópria através do corpo, linguagem e mente, na
dissolução do corpo, após a morte, renasce num estado de privação, num destino
infeliz, até mesmo no inferno. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(INFERNO)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. “Se
fôssemos falar corretamente de alguma coisa que é: ‘absolutamente indesejada,
absolutamente não querida, absolutamente desagradável,’ é do inferno que,
falando corretamente, isso deveria ser dito, tanto assim que é difícil
encontrar um símile para o sofrimento no inferno.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isso
foi dito, um bhikkhu perguntou ao Abençoado: ‘Mas, venerável senhor, pode um
símile ser dado?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. “Pode,
bhikkhu,” o Abençoado disse. [<a href="#N2">2</a>] <a name=R2></a>“Bhikkhus,
suponham que alguns homens prendessem um acusado de roubo e o apresentassem ao
Rei, dizendo: ‘Senhor, aqui está um preso acusado de roubo. Ordene a punição
que deseja para ele.’ Então o rei diria: ‘</span><span lang=PT-BR
style='mso-bidi-font-size:10.0pt;mso-ansi-language:PT-BR'>Vão homens, e, pela
manhã, atirem nele cem lanças.’ Assim, pela manhã, eles atirariam nele cem
lanças. Ao meio dia o rei diria, ‘Homens, como está aquele prisioneiro?’ – ‘Ele
ainda está vivo majestade.’ Então o rei diria, ‘Vão homens, e atirem nele mais
cem lanças.’ Assim, eles atirariam nele mais cem lanças ao meio dia. Ao anoitecer
o rei diria, ‘Homens, como está aquele prisioneiro?’ – ‘Ele ainda está vivo
majestade.’ Então o rei diria, ‘Vão homens, e atirem nele mais cem lanças ao
anoitecer.’ Assim, ao anoitecer, eles atirariam nele mais cem lanças. Agora o
que vocês pensam, bhikkhus: aquele homem, tendo sido atingido por trezentas
lanças ao longo do dia, experimentaria dor e tristeza devido a isso?” </span><span
lang=PT-BR style='mso-ansi-language:PT-BR'><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-bidi-font-size:10.0pt;
mso-ansi-language:PT-BR'>“Venerável senhor, mesmo se ele fosse atingido por
apenas uma lança ele experimentaria dor e tristeza devido a isso, o que não dizer
de trezentas lanças.”</span><span lang=PT-BR style='mso-ansi-language:PT-BR'><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. Então,
tomando uma pequena pedra do tamanho da sua mão, o Abençoado disse aos bhikkhus
o seguinte: “O que vocês pensam bhikkhus? O que é maior, esta pequena pedra que
tomei, do tamanho da minha mão, ou o Himalaia, o rei das montanhas?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, a pequena pedra que o Abençoado tomou, do tamanho da sua mão, não se
compara com o Himalaia, o rei das montanhas; ela não é sequer uma fração, não
há comparação.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Da mesma forma,
bhikkhus, a dor e tristeza que o homem experimentaria por ser atingido por
trezentas lanças não se compara com o sofrimento do inferno; elas não são
sequer uma fração, não há comparação.<o:p></o:p></span></p>



<p class=Normal><a name=T10></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>10. “Agora os guardiões do inferno o torturam com os cinco espetos. Eles
atravessam a mão com um espeto incandescente, eles atravessam a outra mão com
um espeto incandescente, eles atravessam um pé com um espeto incandescente,
eles atravessam o outro pé com um espeto incandescente, eles atravessam a
barriga com um espeto incandescente. Assim ele sente sensações dolorosas,
torturantes e penetrantes. No entanto, ele não morre enquanto aquela ação prejudicial
não tiver esgotado o seu resultado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. “Em
seguida os guardiões do inferno o arremessam ao solo e o aparam com machados.<span
style='mso-spacerun:yes'>  </span>Assim ele sente sensações dolorosas,
torturantes e penetrantes. No entanto, ele não morre enquanto aquela ação prejudicial
não tiver esgotado o seu resultado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. “Em
seguida os guardiões do inferno o colocam com os pés para cima e a cabeça para
baixo e o aparam com enxós. Assim ele sente sensações dolorosas, torturantes e
penetrantes. No entanto, ele não morre enquanto aquela ação prejudicial não tiver
esgotado o seu resultado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. “Em
seguida os guardiões do inferno arreiam-no a uma carruagem e conduzem-no para cá
e para lá pelo chão esbraseado, em chamas e ardente. Assim ele sente
sensações dolorosas, torturantes e penetrantes. No entanto, ele não morre
enquanto aquela ação prejudicial não tiver esgotado o seu resultado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14. “Em
seguida os guardiões do inferno fazem com que ele suba e desça um grande
amontoado de carvão em brasa, em chamas e ardente.<span
style='mso-spacerun:yes'>  </span>Assim ele sente sensações dolorosas,
torturantes e penetrantes. No entanto, ele não morre enquanto aquela ação prejudicial
não tiver esgotado o seu resultado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. “Em
seguida os guardiões do inferno o mergulham com os pés para cima e a cabeça
para baixo num caldeirão com metal fervendo, queimando, em chamas e ardente.
Ele é cozido num redemoinho de vapor. E enquanto ele é ali cozido num
redemoinho de vapor, ele é movido ora para cima, ora para baixo e ora para o
lado. Assim ele sente sensações dolorosas, torturantes e penetrantes. No
entanto, ele não morre enquanto aquela ação prejudicial não tiver esgotado o seu
resultado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16. “Em
seguida os guardiões do inferno o arremessam no Grande Inferno. Agora quanto ao
Grande Inferno, bhikkhus:<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Possui quatro cantos e está construído <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>com quatro portas, uma de cada lado,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>cercado por paredes de ferro em todos os lados<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>e cerrado com um teto de ferro.<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>O piso também é feito de ferro<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>e aquecido até que brilhe como o fogo.<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>A extensão é um total de cem léguas<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>que está envolta de forma completa.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17.
“Bhikkhus, eu poderia descrever o inferno de muitas formas. [<a href="#N3">3</a>]
<a name=R3></a>Tanto assim que é difícil encontrar um símile para o sofrimento
no inferno.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(O REINO
ANIMAL)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18.
“Bhikkhus, há animais que se alimentam de capim. Eles se alimentam cortando o
capim fresco ou seco com os dentes. E quais animais se alimentam de capim? Elefantes,
cavalos, bois, burros, bodes e gamos, e outros animais semelhantes. Um tolo que
antes aqui se deliciava com os sabores e praticou ações prejudiciais, com a dissolução
do corpo, após a morte, renasce na companhia de animais que se alimentam de
capim.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>19. “Há
animais que se alimentam de esterco. Eles cheiram o esterco à distância e
correm na sua direção, pensando: ‘Podemos comer, podemos comer!’ Tal como os
brâmanes que correm para o cheiro de um sacrifício pensando:<span
style='mso-spacerun:yes'>  </span>‘Podemos comer, podemos comer!’ assim<span
style='mso-spacerun:yes'>  </span>também esses animais que se alimentam de
esterco cheiram o esterco à distância e correm na sua direção, pensando:
‘Podemos comer, podemos comer!’ E quais animais se alimentam de esterco? Aves,
porcos, cães e chacais, e outros animais semelhantes. Um tolo que antes aqui se
deliciava com os sabores e praticou ações prejudiciais, com a dissolução do corpo,
após a morte, renasce na companhia de animais que se alimentam de esterco.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>20. “Há
animais que nascem, envelhecem e morrem na escuridão. E quais animais nascem,
envelhecem e morrem na escuridão? Mariposas noturnas, larvas e minhocas, e
outros animais semelhantes. Um tolo que antes aqui se deliciava com os sabores
e praticou ações prejudiciais, com a dissolução do corpo, após a morte, renasce na
companhia de animais que nascem, envelhecem e morrem na escuridão.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>21. “Há
animais que nascem, envelhecem e morrem na água. E quais animais nascem,
envelhecem e morrem na água? Peixes, tartarugas e crocodilos e outros animais
semelhantes. Um tolo que antes aqui se deliciava com os sabores e praticou
ações prejudiciais, com a dissolução do corpo, após a morte, renasce na companhia de
animais que nascem, envelhecem e morrem na água.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>22. “Há
animais que nascem, envelhecem e morrem na imundície. E quais animais nascem,
envelhecem e morrem na imundície? Aqueles animais que nascem, envelhecem e
morrem num peixe apodrecido ou num corpo apodrecido ou numa massa de farinha
apodrecida ou numa fossa ou num esgoto. Um tolo que antes aqui se deliciava com
os sabores e praticou<span style='mso-spacerun:yes'>  </span>ações prejudiciais, com a
dissolução do corpo, após a morte, renasce na companhia de animais que nascem,
envelhecem e morrem na imundície.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>23.
&quot;Bhikkhus, eu poderia descrever o reino animal de muitas formas. Tanto
assim que é difícil encontrar um símile para o sofrimento no reino animal.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>24. “Suponham
que um homem lançasse no oceano uma bóia com um único furo. Um vento do leste a
empurraria para o oeste, um vento do oeste a empurraria para o leste. Um vento
do norte a empurraria para o sul, um vento do sul a empurraria para o norte. E
suponham que houvesse uma tartaruga cega. Ela viria para a superfície uma vez a
cada cem anos. Agora o que vocês pensam: aquela tartaruga cega, vindo para a
superfície uma vez a cada cem anos, colocaria o seu pescoço naquela bóia com um
único furo?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ela
poderia, venerável senhor, uma vez ou outra ao final de um longo período.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
a tartaruga cega demoraria menos tempo para colocar o pescoço naquela bóia com
um único furo do que um tolo, uma vez na perdição, demoraria para recuperar o
estado humano, eu digo. Por que isso? Porque ali não há a prática do Dhamma, não
há a prática do que é virtuoso, não se age de forma benéfica, não se realizam
méritos. Prevalece o devorar mútuo e o massacre dos mais fracos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>25. “Se,
uma vez ou outra, ao final de um longo período, aquele tolo retornar ao estado
humano, será numa família inferior que ele irá renascer<span
style='mso-spacerun:yes'>  </span>- uma família de sudras, de cesteiros de bambu,
de caçadores, de consertadores de carruagens ou de lixeiros – uma família pobre na qual
há pouco para comer e beber e que sobrevive com dificuldades, na qual a comida
e roupas são obtidas com dificuldades; e ele será feio, antiestético,
deformado, doente crônico - peticego ou com as mãos deformadas ou coxo ou
paralítico. Ele não será um daqueles que ganham comida, bebida, roupas e
veículos; grinaldas, perfumes e ungüentos; cama, moradia e lamparinas. Ele se
dedica à conduta imprópria com o corpo, linguagem e mente. Tendo feito isso,
com a dissolução do corpo, após a morte, ele renasce num estado de privação,
num destino infeliz, na perdição, até mesmo no inferno.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>26.
“Bhikkhus, suponham que um jogador infeliz na sua primeira jogada perca o seu
filho e esposa e todas as suas posses, e além disso seja ele mesmo escravizado,
mas mesmo uma jogada infeliz como essa é insignificante; é uma jogada muito
mais infeliz quando um tolo se comporta de forma imprópria através do corpo,
linguagem e mente, e tendo agido assim, na dissolução do corpo, após a morte,
ele renasce num estado de privação, num destino infeliz, na perdição, até mesmo
no inferno. Essa é a completa perfeição do grau dos tolos. [<a href="#N4">4</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'><b>(O SÁBIO)</b><o:p></o:p></span></p>



<p class=Normal><a name=T27></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>27. “Bhikkhus, existem esses três atributos de um sábio, sinais de
um sábio, atributos de um sábio. Quais três? Aqui um sábio é
aquele que pensa pensamentos benéficos, diz palavras benéficas e pratica ações benéficas. Se
um sábio não fosse assim, como poderia o sábio reconhecê-lo desta forma:
‘Esta pessoa é uma pessoa sábia, uma pessoa verdadeira’? Pelo fato de um sábio ser aquele 
que pensa pensamentos benéficos, diz palavras benéficas e pratica ações benéficas, o sábio o reconhece desta forma: ‘Esta pessoa é uma pessoa sábia, uma
pessoa verdadeira.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>28. “Um
sábio sente prazer e alegria aqui e agora de três formas. Se um sábio
estiver sentado numa assembléia, numa rua, ou numa praça, e as pessoas
ali estiverem discutindo certos assuntos relevantes e pertinentes, então, se o
sábio é alguém que se abstém de matar seres vivos, se abstém de tomar aquilo que
não é dado, se abstém de se comportar de forma imprópria em relação aos prazeres
sensuais, se abstém de dizer mentiras e se abstém de se entregar ao vinho, álcool e outros
embriagantes, que causam a negligência, ele pensa: ‘Essas pessoas estão
discutindo certos assuntos relevantes e pertinentes; essas coisas não são
encontradas em mim, e eu não sou visto engajando-me nessas coisas.’ [<a
href="#N5">5</a>] <a name=R5></a>Esse é o primeiro tipo de prazer e alegria que
um sábio sente aqui e agora.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>29.
“Novamente, quando o acusado de um roubo é preso, um sábio vê os reis
aplicando nele diversos tipos de tortura ... (igual ao verso 4) ... Então o
sábio pensa assim: ‘Devido a ações prejudiciais como essas, quando o acusado de
um roubo é preso, os reis aplicam nele diversos tipos de tortura: eles o açoitam
com chicotes ... e o decapitam com uma espada. Essas coisas não são encontradas
em mim, e eu não sou visto engajando-me nessas coisas.’ Esse é o segundo tipo
de prazer e alegria que um sábio sente aqui e agora.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>30.
“Novamente, quando um sábio está na sua cadeira, ou na sua cama, ou
descansando no chão, então, as ações benéficas que ele cometeu no passado – as
condutas apropriadas com o corpo, linguagem e mente – o cobrem, se estendem
sobre ele e o envelopam. Tal como a sombra de uma grande montanha ao anoitecer
cobre, se estende sobre a terra e a envelopa, assim também, quando um sábio está na sua cadeira, 
ou na sua cama, ou descansando no chão, então as ações
benéficas que ele cometeu no passado – as condutas apropriadas com o corpo,
linguagem e mente – o cobrem, se estendem sobre ele e o envelopam. Então, o
sábio pensa: ‘Eu não fiz aquilo que é prejudicial, eu não fiz aquilo que é
cruel, eu não fiz aquilo que é mau. Eu fiz aquilo que é bom, eu fiz aquilo que
é benéfico, eu fiz de mim mesmo um abrigo contra a aflição. Quando falecer, eu
irei para o destino daqueles que não fizeram aquilo que é prejudicial ... que fizeram
de si mesmo um abrigo contra a aflição.’ Ele não se entristece, não fica
angustiado e lamenta, ele não chora batendo no peito e não fica perturbado.
Esse é o terceiro tipo de prazer e alegria que um sábio sente aqui e
agora.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>31. “Um
sábio que se entregou à conduta apropriada através do corpo, linguagem e
mente, na dissolução do corpo, após a morte, renasce num destino feliz, até
mesmo no paraíso. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(PARAÍSO)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>32. “Se
fôssemos falar corretamente de alguma coisa que é: ‘absolutamente desejada,
absolutamente querida, absolutamente agradável,’ é do paraíso que, falando
corretamente, isso deveria ser dito, tanto assim que é difícil encontrar um
símile para a felicidade no paraíso.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isso
foi dito, um bhikkhu perguntou ao Abençoado: ‘Mas, venerável senhor, pode um
símile ser dado?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>33. “Pode,
bhikkhu,” o Abençoado disse. “Bhikkhus, suponham que um Monarca que Gira a Roda
[<a href="#N6">6</a>] <a name=R6></a>possuísse os sete tesouros e os quatro
tipos de poder e devido a isso ele experimentasse prazer e alegria.<o:p></o:p></span></p>



<p class=Normal><a name=T34></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>34. “Quais são os sete tesouros? Aqui, quando um rei nobre ungido
lavou a cabeça no Uposatha do décimo quinto dia [<a href="#N7">7</a>] <a
name=R7></a>e ascendeu até a câmara no topo do palácio para o Uposatha, a Roda
Preciosa surge para ele, com mil raios, com a roda, o cubo, completa em todos
os aspectos. Ao vê-la o rei nobre ungido pensa: ‘Eu ouvi que quando um rei
nobre ungido tiver lavado a cabeça no Uposatha do décimo quinto dia e tiver
ascendido até a câmara no topo do palácio para o Uposatha, e ali aparecer a
roda preciosa com mil raios, com a roda, o cubo, completa em todos os aspectos,
então ele se tornará um Monarca que gira a roda. Eu sou então um Monarca que
gira a roda?’ <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>35. “Então,
levantando-se do seu assento e depois de<span style='mso-spacerun:yes'> 
</span>arrumar o manto externo sobre o ombro, o rei nobre ungido toma um vaso
com água com a mão esquerda, borrifa a roda preciosa com a mão direita e diz:
‘Gire para adiante, boa roda preciosa; triunfe, boa roda preciosa!’ Então a
roda gira para adiante na direção leste e o Monarca que gira a roda a segue com
o seu exército. Agora em qualquer região que a roda pare, ali o Monarca que
gira a roda estabelece residência com o seu exército. E aqueles que antes a ele
se opunham na região leste vêm até o Monarca que gira a roda e dizem: ‘Venha,
grande rei; bem vindo, grande rei; comande, grande rei; aconselhe, grande rei.’
O Monarca que gira a roda assim diz: ‘Vocês não devem matar seres vivos; vocês
não devem tomar aquilo que não for dado; vocês não devem agir de forma
imprópria em relação aos prazeres sensuais; vocês não devem dizer mentiras;
vocês não devem beber bebidas embriagantes; sejam moderados na alimentação.’ E
aqueles que antes a ele se opunham na região leste se tornam seus súditos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então, a
roda mergulha no oceano do leste e emerge outra vez. E nisso, ela gira para
adiante, na direção sul ... E aqueles que se opunham na região sul se submetem
ao Monarca que gira a roda. Então, a roda mergulha no oceano do sul e emerge
outra vez. E nisso, ela gira para adiante, na direção oeste<span
style='mso-spacerun:yes'>  </span>... E aqueles que se opunham na região oeste
se submetem ao Monarca que gira a roda. Então, a roda mergulha no oceano do
oeste e emerge outra vez. E nisso, ela gira para adiante na direção norte ... E
aqueles que se opunham na região norte se submetem ao Monarca que gira a roda.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Agora,
quando a roda preciosa triunfa sobre a terra de oceano a oceano, ela retorna
para a capital real e permanece como que presa pelo eixo ao portão do principal
palácio do Monarca que gira a roda, como um adorno no portão do palácio
principal. Assim é como a roda preciosa aparece para um Monarca que gira a
roda.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>36. “Então,
o elefante precioso aparece para o Monarca que gira a roda, todo branco, com
postura sétupla, com poderes supra-humanos, voando através do espaço, o rei dos
elefantes chamado ‘Uposatha.’ Ao vê-lo, a mente do Monarca que gira a roda tem
confiança nele assim: ‘Seria maravilhoso montar no elefante, se ele se
submetesse ao treinamento!’ Então o elefante precioso se submete ao treinamento
como um fino elefante puro-sangue bem domesticado há muito tempo. E assim
sucede que o Monarca que gira a roda, ao testar o elefante precioso, monta nele
pela manhã e depois de atravessar toda a terra até a margem do oceano, retorna
à capital real para a refeição matinal. Assim é como o elefante precioso
aparece para um Monarca que gira a roda.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>37. “Então,
o cavalo precioso aparece para o Monarca que gira a roda, todo branco, com a
cabeça negra e lustrosa e a crina como a erva munja, com poderes supra-humanos,
voando através do espaço, o rei dos cavalos chamado ‘Valahaka’, [‘Nuvem de
Tormenta’]. Ao vê-lo, a mente do Monarca que gira a roda tem confiança nele assim:
‘Seria maravilhoso montar no cavalo, se ele se submetesse ao treinamento!’
Então o cavalo precioso se submete ao treinamento tal como um fino cavalo
puro-sangue bem domesticado há muito tempo. E assim sucede que o Monarca que
gira a roda, ao testar o cavalo precioso, monta nele pela manhã e depois de
atravessar toda a terra até a margem do oceano, retorna à capital real para a
refeição matinal. Assim é como o cavalo real aparece para um Monarca que gira a
roda.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>38. “Então,
a jóia preciosa aparece para o Monarca que gira a roda. A jóia é um fino berilo
da mais pura água, com oito facetas, bem lapidada. Agora, a luminosidade da
jóia preciosa se espalha ao redor por uma légua. E assim acontece que quando o
Monarca que gira a roda está testando a jóia preciosa, ele coloca em formação o
seu exército e instalando a jóia no topo do seu estandarte, ele sai nas trevas
e escuridão da noite. Então, todos os [habitantes dos] vilarejos próximos
começam o seu trabalho por meio da luz, pensando que é dia. Assim é como a jóia
preciosa aparece para um Monarca que gira a roda.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>39. “Então,
a mulher preciosa aparece para o Monarca que gira a roda, bela, atraente e
graciosa, possuindo a complexão da suprema beleza, nem muito alta nem muito baixa,
nem muito magra nem muito robusta, nem muito escura nem muito clara, superando
a beleza humana, sem alcançar a beleza divina. O toque da mulher preciosa é tal
que se iguala a um tufo de paina ou um tufo de algodão. Quando está frio os
membros dela estão quentes; quando está quente os membros dela estão frios. Do
seu corpo emana o perfume do sândalo e da sua boca o perfume do lótus. Ela se
levanta antes do Monarca que gira a roda e se deita depois dele. Ela é ávida
por servir, com conduta agradável e com a linguagem doce. Como ela nunca é
infiel ao Monarca que gira a roda, sequer em pensamento, como poderia ela sê-lo
com o corpo? Assim é como a mulher preciosa aparece para um Monarca que gira a
roda.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>40. “Então,
o tesoureiro precioso aparece para o Monarca que gira a roda. O olho divino
nascido do kamma passado se manifesta nele e através deste ele vê depósitos
ocultos de tesouros, com e sem proprietários. Ele se aproxima do Monarca que
gira a roda e diz: ‘Senhor, fique tranqüilo. Eu tomarei conta dos seus assuntos
financeiros.’<span style='mso-spacerun:yes'>  </span>E assim acontece quando o
Monarca que gira a roda está testando o tesoureiro precioso, ele embarca num
navio e saindo pelo rio Ganges, no meio da correnteza ele diz para o tesoureiro
precioso: ‘Eu preciso de ouro e lingotes, tesoureiro.’ – ‘Então, senhor, deixe
que o barco seja dirigido para uma das margens.’ – ‘Tesoureiro, na verdade é
aqui que preciso de ouro e lingotes.’ Então o tesoureiro precioso mergulha
ambas as mãos na água e ergue um pote cheio de ouro e lingotes e diz para o
Monarca que gira a roda: ‘Isso é o suficiente, senhor? O oferecido é
suficiente, o ofertado é suficiente?’ – ‘Isso é suficiente, tesoureiro, o
suficiente foi feito, o suficiente foi ofertado.’ Assim é como o tesoureiro
precioso aparece para um Monarca que gira a roda.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>41. “Então,
o conselheiro precioso aparece para o Monarca que gira a roda, sábio, esperto e
sagaz, capaz de fazer com que o Monarca que gira a roda promova aquilo que é
digno de ser promovido, de descartar aquilo que deve ser descartado, e de
estabelecer aquilo que deve ser estabelecido. Ele se aproxima do Monarca que
gira a roda e diz: ‘Senhor, fique tranqüilo. Eu o aconselharei.’ Assim é como o
conselheiro precioso aparece para um Monarca que gira a roda.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Esses são
os sete tesouros que um Monarca que gira a roda possui.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>42. “Quais
são os quatro tipos de poder? Primeiro um Monarca que gira a roda é belo,
atraente e elegante, possuindo a complexão da beleza suprema, e ele supera
todos os outros homens nesse aspecto. Esse é o primeiro tipo de poder que um
Monarca que gira a roda possui.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>43.
“Segundo, um Monarca que gira a roda vive por muito tempo superando todos os
outros homens nesse aspecto. Esse é o segundo tipo de poder que um Monarca que
gira a roda possui.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>44.
“Terceiro, um Monarca que gira a roda está livre das enfermidades e aflições,
possuindo boa digestão que não é nem muito fria, nem muito quente, ele supera
todos os outros homens nesse aspecto. Esse é o terceiro tipo de poder que um
Monarca que gira a roda possui.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>45. “Quarto,
um Monarca que gira a roda é querido e estimado pelos brâmanes e chefes de
família. Tal como um pai é querido e estimado pelos seus filhos, assim também
um Monarca que gira a roda é querido e estimado pelos brâmanes e chefes de
família. Brâmanes e chefes de família também são queridos e estimados pelo
Monarca que gira a roda. Tal como os filhos são queridos e estimados pelo seu pai,
 assim também brâmanes e chefes de família são queridos e estimados pelo
Monarca que gira a roda. Certa vez um Monarca que gira a roda estava num parque
das delícias com o seu exército completo. Então os brâmanes e chefes de família
foram até ele e disseram o seguinte: ‘Senhor, conduza devagar para que possamos
vê-lo por mais tempo.’ E assim ele disse para o seu cocheiro: ‘Cocheiro,
conduza mais devagar para que eu possa ver os brâmanes e chefes de família por
mais tempo.’ Esse é o quarto tipo de poder que um Monarca que gira a roda
possui.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Esses são
os quatro tipos de poder que um Monarca que gira a roda possui.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>46. “O que
vocês pensam, bhikkhus? Um Monarca que gira a roda experimenta prazer e alegria
por possuir esses sete tesouros e esses quatro tipos de poder?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, um Monarca que gira a roda experimentaria prazer e alegria por possuir
apenas um tesouro, o que dizer de sete tesouros e quatro tipos de poder.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>47. Então,
tomando uma pequena pedra do tamanho da sua mão, o Abençoado disse aos bhikkhus
o seguinte: “O que vocês pensam bhikkhus? O que é maior, esta pequena pedra que
tomei, do tamanho da minha mão, ou o Himalaia, o rei das montanhas?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, a pequena pedra que o Abençoado tomou, do tamanho da sua mão, não se
compara com o Himalaia, o rei das montanhas; ela não é nem mesmo uma fração,
não há comparação.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Da mesma
forma, bhikkhus, o prazer e alegria, que um Monarca que gira a roda experimenta
por possuir os sete tesouros e os quatro tipos de poder, não se compara com a
felicidade do paraíso; eles não são nem uma fração, não há comparação.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>48. “Se,
uma vez ou outra, ao final de um longo período, aquele sábio retornar ao
estado humano, é numa família superior que ele irá renascer - uma família
khattiya afluente, uma família brâmane afluente, ou uma família de um chefe de
família afluente – uma família que é rica, com grande riqueza e posses, com
ouro e prata em abundância, com tesouros e mercadorias em abundância, com
riqueza e grãos em abundância; e ele será belo, atraente, possuindo beleza e
complexão supremas. Ele será um daqueles que ganham comida, bebida, roupas e
veículos; grinaldas, perfumes e ungüentos; cama, moradia e lamparinas; ele se
comportará de forma apropriada através do corpo, linguagem e mente, e tendo
agido assim, na dissolução do corpo, após a morte, ele renascerá num destino
feliz, até mesmo no paraíso.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>49. “Bhikkhus,
suponham que um jogador feliz na sua primeira jogada ganhe uma grande fortuna,
mas mesmo uma jogada feliz como essa é insignificante; é uma jogada muito mais
feliz quando um sábio se comporta de forma apropriada através do corpo,
linguagem e mente, e tendo agido assim, na dissolução do corpo, após a morte,
ele renasce num destino feliz, até mesmo no paraíso. Essa é a completa perfeição do grau do sábio. [<a href="#N8">8</a>] <a
name=R8></a><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o
que disse o Abençoado. Os bhikkhus ficaram satisfeitos e contentes com as
palavras do Abençoado.<o:p></o:p></span></p>



