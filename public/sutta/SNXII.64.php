<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em Savathi. &quot;Existem
esses quatro tipos de alimentos para a manutenção dos seres que já nasceram e
para o sustento daqueles que estão em busca de um nascimento. Quais quatro? O
alimento comida, grosseira ou sutil, o contato como o segundo, a volição mental
como o terceiro e a consciência como o quarto. Esses são os quatro tipos de
alimentos para a manutenção dos seres que já nasceram e para o sustento
daqueles que estão buscando o nascimento. <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando
