expande. Quando a consciência se estabelece e expande, a  mentalidade-materialidade (nome e forma)</span><span
lang=PT-BR style='mso-ansi-language:PT-BR'> é estabelecida. Quando a mentalidade-materialidade (nome e forma)</span><span
lang=PT-BR style='mso-ansi-language:PT-BR'> é estabelecida, ocorre o crescimento
das formações volitivas. Quando ocorre o crescimento das formações volitivas, ocorre a produção de
um renovado ser/existir no futuro, há um futuro nascimento, envelhecimento e
morte, juntamente, eu lhes digo, com tristeza, angústia e desespero.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando
existe cobiça, prazer e desejo pelo alimento contato...<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando
existe cobiça, prazer e desejo pelo alimento volição mental...<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando
existe cobiça, prazer e desejo pelo alimento consciência, a consciência ali se
estabelece e expande. Quando a consciência se estabelece e expande, a  mentalidade-materialidade (nome e forma)</span><span
lang=PT-BR style='mso-ansi-language:PT-BR'> é estabelecida. Quando a mentalidade-materialidade (nome e forma)</span><span
lang=PT-BR style='mso-ansi-language:PT-BR'> é estabelecida, ocorre o crescimento
das formações volitivas. Quando ocorre o crescimento das formações volitivas, ocorre a produção de
um renovado ser/existir no futuro, há um futuro nascimento, envelhecimento e
morte, juntamente, eu lhes digo, com tristeza, angústia e desespero.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Como
quando existe pigmento, laca, corante auricolor, índigo, carmesim – um pintor
