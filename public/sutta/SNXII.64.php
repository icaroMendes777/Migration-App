das formações volitivas. Quando ocorre o crescimento das formações volitivas,
ocorre a produção de um renovado ser/existir no futuro, há um futuro
nascimento, envelhecimento e morte, juntamente, eu lhes digo, com tristeza,
angústia e desespero. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando não
