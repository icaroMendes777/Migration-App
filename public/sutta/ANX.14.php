<p class=Normal> "Bhikkhus, qualquer bhikkhu ou bhikkhuni que não tenha abandonado cinco tipos de obstruções na mente e não tenha partido cinco grilhões na mente, então, quer venha o dia ou a noite, apenas a deterioração nas qualidades benéficas deve ser esperada e não o seu crescimento. </p>

<p class=Normal>"Quais são os cinco tipos de obstruções na mente que não foram abandonadas? </p>

<p class=Normal> (1) "Aqui, um bhikkhu tem dúvida, incerteza, indecisão e insegurança com relação ao Mestre e dessa forma a mente dele não se inclina pelo ardor, devoção, perseverança e esforço. Como a mente dele não se inclina pelo ardor, devoção, perseverança e esforço, essa é a primeira obstrução na mente que ele não abandonou. </p>

<p class=Normal> (2) - (5) "Outra vez, um bhikkhu tem dúvida, incerteza, indecisão e insegurança com relação ao Dhamma ... com relação à Sangha ... com relação ao treinamento ... tem raiva e descontentamento dos seus companheiros na vida santa, demonstrando-lhes ressentimento e insensibilidade, e dessa forma a mente dele não se inclina pelo ardor, devoção, perseverança e esforço. Como a mente dele não se inclina pelo ardor, devoção, perseverança e esforço, essa é a quinta obstrução na mente que ele não abandonou. </p>

<p class=Normal>"Esses são os cinco tipos de obstruções na mente que não foram abandonadas. </p>

<p class=Normal>"Quais são os cinco tipos de grilhões na mente que não foram partidos? </p>

<p class=Normal> (1) “Aqui, um bhikkhu não está livre da paixão, desejo, afeição, sede, cobiça e ambição pelos prazeres dos sentidos, e dessa forma a mente dele não se inclina pelo ardor, devoção, perseverança e esforço. Como a mente dele não se inclina pelo ardor, devoção, perseverança e esforço, esse é o primeiro grilhão na mente que ele não partiu. </p>

<p class=Normal> (2) - (5) "Outra vez, um bhikkhu não está livre da paixão, desejo, afeição, sede, cobiça e ambição pelo corpo, ... não está livre da paixão, desejo, afeição, sede, cobiça e ambição pela forma ... come tanto quanto ele quiser, até que a sua barriga esteja cheia e se entrega aos prazeres da indolência, cochilo e sono ... ele vive a vida santa aspirando por algum plano divino assim: ‘Por esta virtude, ou observância, ou ascetismo, ou vida santa, eu me tornarei um [grande] deva ou algum deva [menor], e desse modo a mente dele não se inclina pelo ardor, devoção, perseverança e esforço. Como a mente dele não se inclina pelo ardor, devoção, perseverança e esforço, esse é o quinto grilhão na mente que ele não partiu. </p>

<p class=Normal>"Esses são os cinco tipos de grilhões da mente que não foram partidos. </p>

<p class=Normal>"Qualquer bhikkhu ou bhikkhuni que não tenha abandonado esses cinco tipos de obstruções na mente e não tenha partido esses cinco grilhões na mente, então, quer venha o dia ou a noite, apenas a deterioração nas qualidades benéficas deve ser esperada e não o seu crescimento. Tal como na quinzena escura, quer venha o dia ou a noite, a lua apenas deteriora em beleza, circularidade, e brilho, em diâmetro e circunferência, assim também, qualquer bhikkhu ou bhikkhuni que não tenha abandonado esses cinco tipos de obstruções ... apenas a deterioração nas qualidades benéficas deve ser esperada e não o seu crescimento. </p>

<p class=Normal>"Bhikkhus, qualquer bhikkhu ou bhikkhuni que tenha abandonado cinco tipos de obstruções na mente e tenha partido cinco grilhões na mente, então, quer venha o dia ou a noite, apenas o crescimento nas qualidades benéficas deve ser esperado e não a sua deterioração. </p>

<p class=Normal>"Quais são os cinco tipos de obstruções na mente que foram abandonadas? </p>

<p class=Normal> (1) "Aqui, um bhikkhu não tem dúvida, incerteza, indecisão e insegurança com relação ao Mestre e dessa forma a mente dele se inclina pelo ardor, devoção, perseverança e esforço. Como a mente dele se inclina pelo ardor, devoção, perseverança e esforço, essa é a primeira obstrução na mente que ele abandonou. </p>

<p class=Normal> (2) - (5) "Outra vez, um bhikkhu não tem dúvida, incerteza, indecisão e insegurança com relação ao Dhamma ... com relação à Sangha ... com relação ao treinamento ... não tem raiva mas contentamento com relação aos seus companheiros na vida santa, sem demonstrar-lhes ressentimento, com boa disposição, e dessa forma a mente dele se inclina pelo ardor, devoção, perseverança e esforço. Como a mente dele se inclina pelo ardor, devoção, perseverança e esforço, essa é a quinta obstrução na mente que ele abandonou. </p>

<p class=Normal>"Esses são os cinco tipos de obstruções na mente que foram abandonadas. </p>

<p class=Normal>"Quais são os cinco tipos de grilhões na mente que foram partidos? </p>

<p class=Normal> (1) “Aqui, um bhikkhu está livre da paixão, desejo, afeição, sede, cobiça e ambição pelos prazeres dos sentidos, e dessa forma a mente dele se inclina pelo ardor, devoção, perseverança e esforço. Como a mente dele se inclina pelo ardor, devoção, perseverança e esforço, esse é o primeiro grilhão na mente que ele partiu. </p>

<p class=Normal> (2) - (5) "Outra vez, um bhikkhu está livre da paixão, desejo, afeição, sede, cobiça e ambição pelo corpo, ...  está livre da paixão, desejo, afeição, sede, cobiça e ambição pela forma ... não come tanto quanto ele quiser, até que a sua barriga esteja cheia e não se entrega aos prazeres da indolência, cochilo e sono ... ele não vive a vida santa aspirando por algum plano divino assim: ‘Por esta virtude, ou observância, ou ascetismo, ou vida santa, eu me tornarei um [grande] deva ou algum deva [menor], e desse modo a mente dele se inclina pelo ardor, devoção, perseverança e esforço. Como a mente dele se inclina pelo ardor, devoção, perseverança e esforço, esse é o quinto grilhão na mente que ele partiu. </p>

<p class=Normal>"Esses são os cinco tipos de grilhões da mente que foram partidos. </p>

<p class=Normal>"Qualquer bhikkhu ou bhikkhuni que tenha abandonado esses cinco tipos de obstruções na mente e tenha partido esses cinco grilhões na mente, então, quer venha o dia ou a noite, apenas o crescimento nas qualidades benéficas deve ser esperado e não a sua deterioração. Tal como na quinzena luminosa, quer venha o dia ou a noite, a lua apenas aumenta em beleza, circularidade, e brilho, em diâmetro e circunferência, assim também, qualquer bhikkhu ou bhikkhuni que tenha abandonado esses cinco tipos de obstruções ... apenas o crescimento nas qualidades benéficas deve ser esperada e não a sua deterioração. </p>


