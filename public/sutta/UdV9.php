<p class=Normal>Assim ouvi. Em certa ocasião o Abençoado estava perambulando por Kosala com uma grande sangha de bhikkhus. Agora naquela ocasião um grande grupo de jovens brâmanes passava não muito longe do Abençoado fazendo grande zombaria. O Abençoado então viu aquele grande grupo de jovens brâmanes passando não muito longe e fazendo grande zombaria.</p>



