<p class=Normal> “Bhikkhus, há essas seis recompensas ao realizar o fruto de entrar na correnteza. Quais seis? Ele tem certeza do verdadeiro Dhamma. Ele não estará sujeito a decair. Ele estabeleceu um limite para o sofrimento.[<a href="#N1">1</a>] <a name="R1"> </a>Ele possui conhecimento incomum. [<a href="#N2">2</a>] <a name="R2"> </a>Ele compreende com clareza as causas e os fenômenos que surgem com base nas causas.” </p>

<p class=Normal> “Essas são as seis recompensas ao realizar o fruto de entrar na correnteza.” </p>

