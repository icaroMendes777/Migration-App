<p class=Normal>Assim ouvi. Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no Parque de Anathapindika.</p>



<p class=Normal>Então, inúmeros bhikkhus estavam sentados no salão de reuniões, onde se reuniram após a refeição, depois de terem esmolado alimentos, quando esta conversa teve início: “Quem, amigos, conhece algum ofício? Quem, amigos, foi treinado em qual ofício? Qual é o melhor dos ofícios?”</p>



<p class=Normal>Alguns bhikkhus responderam: "O ofício da condução de elefantes é o melhor dos ofícios," enquanto alguns outros disseram: "O ofício da condução de cavalos é o melhor dos ofícios," e outros disseram: "O ofício das carruagens é o melhor dos ofícios," e outros disseram: "O ofício dos arqueiros é o melhor dos ofícios," e outros disseram: "O ofício das espadas é o melhor dos ofícios," e outros disseram: "O ofício de contar com os dedos é o melhor dos ofícios," e outros disseram: "O ofício de contar com o ábaco é o melhor dos ofícios," e outros disseram: "O ofício da contabilidade é o melhor dos ofícios," e outros disseram: "O ofício da escrita é o melhor dos ofícios," e outros disseram: "O ofício da poesia é o melhor dos ofícios," e outros disseram: "O ofício das ciências naturais é o melhor dos ofícios," e outros disseram: "O ofício das ciências políticas é o melhor dos ofícios". </p>



<p class=Normal>Esta era a conversa que teve início entre os bhikkhus e foi interrompida.</p>



<p class=Normal>Então, ao anoitecer, o Abençoado levantou da meditação e foi até o salão de reuniões e sentou num assento que havia sido preparado. Então, ele se dirigiu aos bhikkhus assim: “Bhikkhus, por qual conversa vocês estão reunidos aqui agora? E qual foi a conversa que foi interrompida?”</p>



<p class=Normal>"Aqui, venerável senhor, estávamos sentados no salão de reuniões, onde nos reunimos após a refeição, depois de termos esmolado alimentos, quando esta conversa teve início: “Quem, amigos, conhece algum ofício? Quem, amigos, foi treinado em qual  ofício? Qual é o melhor dos ofícios?”</p>



<p class=Normal>"Não é certo, bhikkhus, que filhos de boas famílias, tendo adotado a vida santa pela fé, se engajem nesse tipo de conversa. Quando vocês se reunirem, bhikkhus, vocês devem fazer uma de duas coisas: discutir o Dhamma ou observar o nobre silêncio.”</p>



<p class=Normal>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p>



	<p class=Normal style='margin-left:.75in'>"Um bhikkhu é aquele que se mantém sem um ofício, <br>

	leve e bem intencionado, com as faculdades contidas,<br> 

	um sem-teto, livre em todos os sentidos,<br>	

	desapegado e livre do desejo,<br> 

	tendo derrotado Mara, ele segue só.”<br>


