<p class=Normal>Assim ouvi. Em certa ocasião, o Abençoado estava em Savatthi no Bosque de Jeta, no Parque de Anathapindika. Agora, naquela ocasião os veneráveis Sariputta, Mahamoggallana, Mahakassapa, Mahakaccayana, Mahakotthita, Mahakappina, Mahacunda, Anuruddha, Revata, e Nanda foram até o Abençoado.</p>



<p class=Normal>O Abençoado viu estes veneráveis vindo à distância e, tendo os visto, ele se dirigiu aos bhikkhus e disse: “Bhikkhus, estes que se aproximam são brâmanes. Bhikkhus, estes que se aproximam são brâmanes.”</p>



<p class=Normal>Quando isto foi dito, um certo bhikkhu originalmente nascido na casta brâmane disse ao Abençoado, “De que forma, venerável senhor alguém é um brâmane? Mais ainda, o que faz desse alguém um brâmane?”</p>



<p class=Normal>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p>



	<p class=Normal style='margin-left:.75in'>“Aqueles que eliminaram as más qualidades<br> 

	e permanecem com atenção plena.<br> 

	Despertos e livres dos grilhões.<br>

	Realmente são estes os brâmanes neste mundo.”</p>

