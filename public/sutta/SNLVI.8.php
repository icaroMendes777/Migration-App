<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
não reflitam de modo ruim e prejudicial: ‘O mundo é eterno’ ou ‘O mundo não é
eterno’; ou ‘O mundo é finito’, ou ‘O mundo é infinito’; ou ‘A alma e o corpo
são a mesma coisa’, ou ‘A alma é uma coisa, o corpo é outra’; ou ‘Após a morte
um Tathagata existe,’ ou ‘Após a morte um Tathagata não existe,’ ou ‘Após a
morte um Tathagata tanto existe como não existe,’ ou ‘Após a morte um Tathagata
nem existe, nem não existe.’ Por qual razão? Porque essas reflexões, bhikkhus,
não trazem benefícios, não pertencem aos fundamentos da vida santa e não
conduzem ao desencantamento, ao desapego, à cessação, à paz, ao conhecimento
direto, à iluminação, a Nibbana<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando
refletirem, bhikkhus, vocês deveriam refletir: ‘Isto é sofrimento’; vocês
deveriam refletir: ‘Esta é a origem do sofrimento’; vocês deveriam refletir: ‘Esta
é a cessação do sofrimento’; vocês deveriam refletir: ‘Este é o caminho que
conduz à cessação do sofrimento.’ Por qual razão? Porque essas reflexões,
bhikkhus, trazem benefícios, pertencem aos fundamentos da vida santa e conduzem
ao desencantamento, ao desapego, à cessação, à paz, ao conhecimento direto, à
iluminação, a Nibbana.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
bhikkhus, um esforço deve ser feito para compreender: ‘Isto é sofrimento.’ <a
name=R2></a>Um esforço deve ser feito para compreender: ‘Esta é a origem do
sofrimento.’ Um esforço deve ser feito para compreender: ‘Esta é a cessação do
sofrimento.’ Um esforço deve ser feito para compreender: ‘Este é o caminho que
conduz à cessação do sofrimento.’”<o:p></o:p></span></p>

