<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim ouvi.
Em certa ocasião o Abençoado estava<span style="mso-spacerun: yes"> 
</span>entre os Angas, numa cidade denominada Apana. Lá ele se dirigiu ao
Venerável Sariputta desta forma:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sariputta,
o nobre discípulo que tem completa dedicação ao Tathagata e tem plena convicção
nele possui alguma dúvida ou perplexidade em relação ao Tathagata ou aos seus ensinamentos?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, o nobre discípulo que tem completa dedicação ao Tathagata e tem plena
convicção nele não possui nenhuma dúvida ou perplexidade em relação ao
Tathagata ou aos seus ensinamentos. É deveras de se esperar, venerável senhor, que
um nobre discípulo, que tem fé, permanecerá com a energia estimulada para o
abandono de qualidades mentais prejudiciais e a obtenção de qualidades mentais
benéficas; ele é decidido, firme no seu esforço, não se esquivando da sua
responsabilidade de cultivar qualidades mentais benéficas. Essa energia que ele
possui, venerável senhor, é a faculdade da energia dele .<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“É deveras
de se esperar, venerável senhor, que um nobre discípulo, que tem fé, terá
atenção plena, possuindo suprema atenção plena e discriminação, alguém que lembre
e se recorde daquilo que foi feito e dito há muito tempo. Essa atenção plena
que ele possui, venerável senhor, é a faculdade da atenção plena dele.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“É deveras
de se esperar, venerável senhor, que um nobre discípulo que tem fé, cuja
energia está estimulada e cuja atenção plena está estabelecida, obterá a
concentração, obterá a unicidade da mente, tendo se soltado do objeto. Essa
concentração que ele possui, venerável senhor, é a faculdade da concentração
dele.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“É deveras
de se esperar, venerável senhor, que um nobre discípulo, que tem fé, cuja
energia está estimulada, cuja atenção plena está estabelecida e cuja mente está
concentrada, compreenderá assim: ‘Esse samsara não possui um início que possa
ser descoberto. Um ponto inicial não é discernido para os seres que seguem
vagando e perambulando, obstaculizados pela ignorância e agrilhoados pelo desejo.
Mas o desaparecimento sem deixar qualquer vestígio e cessação da ignorância,
essa massa de obscuridade: esse é o estado pacífico, esse é o estado sublime,
isto é, o silenciar de todas as formações, o abandono de todas as aquisições, a
destruição do desejo, o desapego, a cessação, Nibbana.’ Essa sabedoria que ele
possui, venerável senhor, é a faculdade da sabedoria dele.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E,
venerável senhor, quando ele se esforçou dessa maneira, repetidas vezes, se
recordou dessa maneira, repetidas vezes, concentrou a mente dessa maneira,
repetidas vezes, compreendeu através da sabedoria dessa maneira, repetidas
vezes, esse nobre discípulo obtém completa convicção desta forma: ‘Quanto
àquelas coisas a respeito das quais eu anteriormente só havia ouvido, eu agora
permaneço contactando-as com o corpo [<a href="#N1">1</a>] <a name=R1></a>e
penetrando-as através da sabedoria, eu vejo.’ Essa convicção que ele possui,
venerável senhor, é a sua faculdade da convicção.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Muito bem,
Sariputta! Sariputta, o nobre discípulo, que tem completa dedicação ao
Tathagata e tem plena convicção nele, não possui nenhuma dúvida ou perplexidade
em relação ao Tathagata ou aos seus ensinamentos.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(O Buda
repete verbatim as faculdades do nobre discípulo descritas pelo Venerável
Sariputta)<o:p></o:p></span></p>




