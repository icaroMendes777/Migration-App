<p class=Normal><a name=R1></a>Certa ocasião o Abençoado estava em Alavi, sentado num amontoado de folhas espalhadas sobre uma trilha de gado, num bosque de simsapa. Então Hatthaka de Alavi [<a href="#N1">1</a>] enquanto caminhava e perambulava fazendo exercício viu o Abençoado ali sentado. Ele foi até o Abençoado e depois de cumprimentá-lo, sentou a um lado e disse: </p>

<p class=Normal>"Venerável senhor, o Abençoado dormiu bem?" </p>

<p class=Normal>"Sim, príncipe, eu sou um daqueles no mundo que dorme bem." </p>

<p class=Normal><a name=R2></a>"Mas, venerável senhor, as noites no inverno são frias. Estamos no intervalo de oito dias, na época em que a neve cai. [<a href="#N2">2</a>] O chão pisoteado pelos cascos do gado é desigual, a camada de folhas é fina, as folhas nas árvores são esparsas, os mantos de cor ocre não aquecem e os ventos fortes são frios. No entanto o Abençoado assim diz: 'Sim, príncipe, eu sou um daqueles no mundo que dorme bem.'"</p>

<p class=Normal>"Muito bem, então príncipe, eu o questionarei acerca desse assunto e você responda como considerar adequado. O que você pensa, príncipe? Um chefe de família, ou o filho de um chefe de família, pode ter uma casa com um telhado com cumeeira, com as paredes revestidas por dentro e por fora, cerrada, protegida por barras, com as janelas fechadas. Lá ele poderá ter um sofá com tapetes, cobertores e colchas, com uma excelente cobertura com pele de antílope e com baldaquino e almofadas vermelhas para a cabeça e os pés. Uma lamparina estaria ardendo e as suas quatro esposas o estariam servindo de um modo extremamente agradável. O que você pensa: ele dormiria bem, ou não, ou o que você pensa com relação a isso?" </p>

<p class=Normal>"Ele dormiria bem, venerável senhor. Ele seria um daqueles no mundo que dorme bem." </p>

<p class=Normal> (1) "O que você pensa, príncipe? Poderiam surgir no chefe de família, ou no filho de um chefe de família, febres corporais e mentais nascidas da paixão, que o atormentariam de modo que ele dormiria mal?" </p>

<p class=Normal>"Sim, venerável senhor." </p>

<p class=Normal>"Poderiam surgir no chefe de família, ou no filho de um chefe de família, febres corporais e mentais nascidas da paixão, que o atormentariam de modo que ele dormiria mal; mas o Tathagata abandonou essa paixão, cortou-a pela raiz, tal como com um tronco de palmeira, eliminada de modo que não estará mais sujeita a um futuro surgimento. Por conseguinte, eu durmo bem. </p>

<p class=Normal> (2) "O que você pensa, príncipe? Poderiam surgir no chefe de família, ou no filho de um chefe de família, febres corporais e mentais nascidas da raiva ... (3) ... febres corporais e mentais nascidas da delusão, que o atormentariam de modo que ele dormiria mal?" </p>

<p class=Normal>"Sim, venerável senhor." </p>

<p class=Normal>"Poderiam surgir no chefe de família, ou no filho de um chefe de família febres corporais e mentais nascidas da raiva ... da delusão, que o atormentariam de modo que ele dormiria mal; mas o Tathagata abandonou essa raiva ... essa delusão, cortou-a pela raiz, tal como com um tronco de palmeira, eliminada de modo que não estará mais sujeita a um futuro surgimento. Por conseguinte, eu durmo bem." </p>

<p class=Normal>Ele sempre dorme bem,<br>
o brâmane que realizou nibbana,<br>
esfriado, sem aquisições,<br>
sem ser maculado pelos prazeres dos sentidos. </p>

<p class=Normal>Tendo extirpado todos os apegos,<br>
tendo removido a tristeza do coração,<br>
quem está em paz dorme bem, <br>
tendo alcançado a paz na mente. </p>

