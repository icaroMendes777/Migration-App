<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, terríveis são o ganho, a honraria e fama, amargos, vis,
obstruem a realização da insuperável segurança contra o cativeiro. Suponham que
houvesse um besouro, que se alimenta de estrume, cheio de estrume, farto de
estrume, e na frente dele estivesse um grande amontoado de estrume. Devido a
isso ele desprezaria os outros besouros pensando: ‘Eu sou um comedor de
estrume, cheio de estrume, saciado de estrume, e na minha frente está um grande
amontoado de estrume.’ Da mesma forma, bhikkhus, um bhikkhu, cuja mente está
dominada e obcecada pelo ganho, honraria e fama, se veste ao amanhecer e
tomando a tigela e o manto externo, entra no vilarejo ou cidade para esmolar
alimentos. Lá ele comeria o tanto que quisesse, seria convidado para a refeição
do dia seguinte e a sua esmola de alimentos seria abundante. Ao regressar ao
monastério, ele se gaba ante um grupo de bhikkhus: ‘Eu comi o tanto que quis,
eu fui convidado para a refeição de amanhã e a minha comida esmolada é
abundante. Eu sou aquele que ganha mantos, comida esmolada, moradia e
medicamentos, mas aqueles outros bhikkhus que têm pouco mérito e influência,
eles não ganham mantos, comida esmolada, moradia e medicamentos.’ Assim, porque
a mente dele está tomada e obcecada pelo ganho, honraria e fama, ele
despreza os outros bhikkhus bem comportados. Isso causará dano e sofrimento
para essa pessoa tola, por muito tempo. Tão terríveis, bhikkhus, são o ganho, a
honraria e fama, amargos, vis, obstruem a realização da insuperável
segurança contra o cativeiro. Portanto, bhikkhus, vocês devem praticar da seguinte
forma: ‘Nós abandonaremos o ganho, a honraria e fama que já surgiram e não
permitiremos que o ganho, a honraria e fama que já surgiram persistam
obcecando as nossas mentes.’ Assim vocês deveriam praticar.”<b
style='mso-bidi-font-weight:normal'><o:p></o:p></b></span></p>


