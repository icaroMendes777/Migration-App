<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
tudo aquilo que não é seu, abandonem-no. [<a href="#N1">1</a>] <a name=R1></a>Ao
abandoná-lo, isso irá conduzir ao seu bem-estar e felicidade por muito tempo. E
o que, bhikkhus, não é seu? O olho não é seu, abandonem-no. Ao abandoná-lo,
isso irá conduzir ao seu bem-estar e felicidade por muito tempo. As formas não
são suas ... A consciência no olho não é sua ... O contato no olho não é seu
... Qualquer sensação que surja, tendo o contato no olho como condição -
experimentada como prazer, dor ou nem prazer, nem dor, também não é sua,
abandonem-na. Ao abandoná-la, isso irá conduzir ao seu bem-estar e felicidade
por muito tempo. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O ouvido não é seu ... A
mente não é sua ... Os objetos mentais não
são seus ... A consciência na mente não é sua ... O contato na mente não é seu ... Qualquer sensação que surja, tendo o contato na mente como
condição - experimentada como prazer, dor ou nem prazer, nem dor, também não é
sua, abandonem-na. Ao abandoná-la, isso irá conduzir ao seu bem-estar e
felicidade por muito tempo. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Suponham, bhikkhus, que </span>as
pessoas levassem embora a grama, gravetos, galhos e folhas deste bosque de
Jeta, ou se os queimassem, ou fizessem com eles o que desejassem, vocês
pensariam: ‘As pessoas estão nos levando ou estão nos queimando ou estão
fazendo conosco o que desejam?’<span style='mso-ansi-language:PT-BR'> <span
lang=PT-BR><o:p></o:p></span></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não, venerável senhor.
Porque não? </span>Porque isso não é nem nosso eu, nem pertence ao nosso eu<span
lang=PT-BR style='mso-ansi-language:PT-BR'>.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Da mesma forma, bhikkhus,
O olho não é seu, abandonem-no ... Qualquer sensação que surja, tendo o contato
na mente como condição - experimentada como prazer, dor ou nem prazer, nem dor,
também não é sua, abandonem-na. Ao abandoná-la, isso irá conduzir ao seu
bem-estar e felicidade por muito tempo.” <o:p></o:p></span></p>


