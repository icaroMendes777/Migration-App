<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em certa
ocasião, o Venerável Mahamoggallana estava em Savatthi no Bosque de Jeta, no
Parque de Anathapindika.<span style="mso-spacerun: yes">  </span>Lá ele se
dirigiu aos monges desta forma: “Amigos, bhikkhus” – “Amigo,” eles responderam.
O Venerável Mahamoggallana disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
amigos, enquanto eu estava só em isolamento, o seguinte pensamento surgiu na
minha mente: ‘Dizem, “o primeiro jhana, o primeiro jhana.” O que é então o
primeiro jhana?’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
amigos, me ocorreu: ‘Aqui, um bhikkhu afastado dos prazeres sensuais, afastado das
qualidades não hábeis, entra e permanece no primeiro jhana, que é
caracterizado pelo pensamento aplicado e sustentado, com o êxtase e felicidade
nascidos do afastamento. Isso é chamado o primeiro jhana.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
amigos, afastado dos prazeres sensuais, afastado das qualidades não hábeis, eu
entrei e permaneci no primeiro jhana ... Enquanto assim permanecia, a atenção e
a percepção acompanhadas pela sensualidade me assediaram. [<a href="#N1">1</a>]
<o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Então, amigos, o Abençoado através dos poderes supra-humanos vinha até
mim e dizia: ‘Moggallana, Moggallana, não seja negligente, brâmane, com relação
ao primeiro jhana. Estabilize a sua mente no primeiro jhana, unifique a sua
mente no primeiro jhana, concentre a sua mente no primeiro jhana.’ Então,
amigos, numa outra ocasião, afastado dos prazeres sensuais, afastado das
qualidades não hábeis, eu entrei e permaneci no primeiro jhana, que é
caracterizado pelo pensamento aplicado e sustentado, com o êxtase e felicidade
nascidos do afastamento.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigos,
falando corretamente, se fosse para dizer de alguém que: ‘Ele é um discípulo
que alcançou a magnitude do conhecimento direto com o auxílio do Mestre,’ é de
mim que, falando o que é certo, isso deveria ser dito.”<o:p></o:p></span></p>

