<p class=Normal><a name=R1></a>"Bhikkhus, eu lhes ensinarei o nível da pessoa falsa e o nível da pessoa verdadeira. [<a href="#N1">1</a>]  Ouçam e prestem muita atenção àquilo que eu vou dizer.” – “Sim, venerável senhor,” os bhikkhus responderam. O Abençoado disse o seguinte:</p>

  <p class=Normal>"E qual é o nível da pessoa falsa? Uma pessoa falsa é ingrata, não reconhece a ajuda que lhe é dada. Essa ingratidão, essa falta de reconhecimento é o costume entre as pessoas falsas. Isto está totalmente de acordo com o nível das pessoas falsas.</p>

<p class=Normal>"E qual é o nível da pessoa verdadeira? Uma pessoa verdadeira é grata, e reconhece a ajuda que lhe é dada. Essa gratidão, esse reconhecimento é o costume entre as pessoas verdadeiras. Isto está totalmente de acordo com o nível das pessoas verdadeiras."</p>  
