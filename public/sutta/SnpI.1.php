<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Estas notas
foram elaboradas tomando por base o livro <i style='mso-bidi-font-style:normal'>The
Vision of Dhamma</i> escrito pelo Venerável Nyanaponika Thera, editado pela
Buddhist Publication Society em 1994.<o:p></o:p></span></p>



<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] Em cada verso deste poema o abandono do aqui e além mencionado em
cada refrão está conectado ao abandono de uma determinada contaminação mental, <i
style='mso-bidi-font-style:normal'>(kilesa)</i>, mencionada na primeira linha
de cada verso. A razão para essa conexão é porque apenas quando as
contaminações forem eliminadas por completo será possível superar a dicotomia
inerente a essas contaminações e transcender o ciclo de renascimentos, que é
deixado para trás como algo vazio, sem substância e estranho – igual à pele
gasta da serpente.[<a href="#R1">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[2] ‘Aqui e além’, <i style='mso-bidi-font-style:normal'>ora-param</i>
em Pali, no seu sentido original significa as duas margens de um rio. ‘Aqui’ é
a margem deste lado do rio que representa a presente existência. ‘Além’
representa qualquer mundo depois da presente existência determinado pelos
frutos de <i style='mso-bidi-font-style:normal'>kamma</i>. Essa frase ‘aqui e
além’ também se aplica a todas aquelas dualidades, discriminações, dicotomias e
pares de opostos que são o hábitat usual da mente e que são a fonte das
decepções, desapontamentos e sofrimento. Talvez o mais proeminente desses
opostos seja a idéia de superior e inferior. Aquilo que é considerado como
‘superior’, e que por isso gera apego, está sujeito à mudança e por isso, de
modo inevitável, irá conduzir ao sofrimento. O abandono desses opostos é um
tema recorrente nos suttas do Sutta Nipata. Os comentários explicam o refrão que
ocorre ao final de cada verso da seguinte forma: de tempos em tempos as
serpentes trocam de pele e esse processo ocorre seguindo as leis da natureza
dessa espécie. Em determinado ponto, quando apenas parte da pele velha foi
removida, a serpente sente repulsa pela pele gasta. Dado essa repulsa a
serpente irá enrolar a cauda num objeto de apoio e fazendo um esforço irá
remover do corpo a pele gasta. Em seguida ela estará livre para fazer o que
queira. De modo semelhante com um bhikkhu. A lei da natureza para um bhikkhu é
a prática da virtude, <i style='mso-bidi-font-style:normal'>(sila)</i>,
dando-se conta do sofrimento inerente no ciclo de renascimentos e na dualidade
de modo geral, representados pelas palavras ‘aqui e além’, ele sente repulsa
por tudo isso. Buscando apoio num nobre amigo ele se esforça da maneira correta
e através do desapego realiza a completa libertação, da mesma forma que a
serpente abandona a sua pele gasta. [<a href="#R2">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[3] A cobiça, <i style='mso-bidi-font-style:normal'>(raga)</i>, é
comparada às flores de lótus como um símbolo da beleza. Devido ao seu
encantamento é fácil esquecer que os prazeres sensuais em pouco tempo perdem a
sua vitalidade e atração. Aquele que se liberta da cobiça está livre dos seus
“dois lados”, a atração e repulsão, gosto e desgosto. Ele também abandonou o
aqui e o além. [<a href="#R3">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[4] A poderosa torrente do desejo, <i style='mso-bidi-font-style:normal'>(tanha)</i>,
que flui através do ciclo de renascimentos. Desejo pelos prazeres sensuais,
desejo por ser/existir e desejo por não ser/existir.[<a href="#R4">Retorna</a>]
<o:p></o:p></span></p>



<p class=Normal><a name=N5></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[5] A presunção ou orgulho humano é comparado a uma frágil ponte de
bambu, que pode facilmente ser abalado pela opinião ou comentários dos outros.
[<a href="#R5">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[6] Da mesma forma que pessoas ignorantes buscam por flores onde estas
não existem, desde tempos imemoriais os seres humanos buscam em vão por alguma
essência ou substância permanente em si mesmos, ou no mundo que habitam, ou
alimentaram esperanças de encontrá-la num mundo mais além. Essa é a busca por
um refúgio seguro que sirva de contrapeso para o medo da morte e que satisfaça
o desejo de auto-preservação. [<a href="#R6">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N7></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[7] O rancor pode ser pessoal ou impessoal. Pessoal quando é uma reação
aos conflitos que surgem das relações humanas. Impessoal quando é uma reação às
inevitáveis vicissitudes da vida e que pode se converter num ressentimento
amargo com relação à vida, ao sentir que a vida o tem tratado de forma injusta.
[<a href="#R7">Retorna</a>] <o:p></o:p></span></p>



