<p class=Normal> "Bhikkhus, possuindo três fatores um excelente cavalo puro sangue de um rei é digno do rei, de servir ao rei, considerado como um dos fatores de um rei. Quais três? Aqui, um excelente cavalo puro sangue de um rei possui beleza, força e velocidade. Possuindo esses três fatores, um excelente cavalo puro sangue de um rei é digno do rei, de servir ao rei, considerado como um dos fatores de um rei. </p>

<p class=Normal>"Do mesmo modo, possuindo três fatores um bhikkhu é merecedor de dádivas, merecedor de hospitalidade, merecedor de oferendas, merecedor de saudações com reverência, um campo inigualável de mérito para o mundo. Quais três? Aqui um bhikkhu possui beleza, força e velocidade. </p>

<p class=Normal>"E como um bhikkhu possui beleza? Aqui, um bhikkhu é virtuoso. Ele permanece contido de acordo com o Patimokkha, perfeito na conduta e na sua esfera de atividades. Temendo a menor falha, ele treina adotando os preceitos de virtude. É desse modo que um bhikkhu possui beleza. </p>

<p class=Normal>"E como um bhikkhu possui força? Aqui, um bhikkhu permanece com a sua energia desperta para o abandono de qualidades mentais prejudiciais e para a obtenção de qualidades mentais benéficas; ele é decidido, firme no seu esforço, sem se esquivar das suas responsabilidades com relação ao cultivo de qualidades mentais benéficas. É desse modo que um bhikkhu possui força. </p>

<p class=Normal> (96) "E como um bhikkhu possui velocidade? Aqui, um bhikkhu compreende como na verdade é 'Isto é sofrimento,' 'Esta é a origem do sofrimento,' 'Esta é a cessação do sofrimento,' 'Este é o caminho que conduz à cessação do sofrimento.' É desse modo que um bhikkhu possui velocidade. </p>

<p class=Normal> (97) "E como um bhikkhu possui velocidade? Aqui, com a completa destruição dos cinco primeiros grilhões ele é um daqueles que não retorna, renascendo em um dos mundos de Brahma para lá realizar o parinibbana, nunca mais retornando desse mundo. É desse modo que um bhikkhu possui velocidade. </p>

<p class=Normal> (98) "E como um bhikkhu possui velocidade? Aqui, com a completa destruição das impurezas mentais, ele permanece em um estado livre de impurezas com a libertação da mente e a libertação através da sabedoria, tendo conhecido e manifestado isso para ele mesmo no aqui e agora. É desse modo que um bhikkhu possui velocidade. </p>



<p class=Normal>"Possuindo essas três qualidades um bhikkhu é merecedor de dádivas, merecedor de hospitalidade, merecedor de oferendas, merecedor de saudações com reverência, um campo inigualável de mérito para o mundo."</p>

