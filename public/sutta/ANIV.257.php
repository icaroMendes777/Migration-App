<p class=Normal> <a name=R1></a> Então, o venerável Malunkyaputta foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse: [<a href="#N1">1</a>]</p>

<p class=Normal> “Venerável senhor, seria bom se o Abençoado pudesse me ensinar o Dhamma de maneira resumida, de modo que, tendo ouvido o Dhamma do Abençoado, eu possa permanecer só, isolado, diligente, ardente e decidido.” </p>

<p class=Normal> “E agora, Malunkyaputta, o que devo dizer aos bhikkhus mais jovens quando um bhikkhu como você, velho, envelhecido, pressionado pelos anos, com a idade avançada, chegando ao último estágio da vida – me pede uma exortação resumida?” </p>

<p class=Normal> “Venerável senhor, que o Abençoado me ensine o Dhamma de forma resumida, que o Iluminado me ensine o Dhamma de forma resumida. É bem possível que eu entenda as palavras do Abençoado. É bem possível que eu me torne um herdeiro das palavras do Abençoado.” </p>

<p class=Normal> "Malunkyaputta, há essas quatro fontes de desejo para um bhikkhu. Quais quatro? O desejo surge num bhikkhu devido a mantos, comida esmolada, moradia, ou devido a esta vida ou a vida mais além. Essas são as quatro fontes de desejo para um bhikkhu. Quando, Malunkyaputta, um bhikkhu abandonou o desejo, cortou-o pela raiz, como um tronco de palmeira, eliminando-o de tal forma que não estará mais sujeito a um futuro surgimento, ele é chamado um bhikkhu que eliminou o desejo, rompeu o grilhão e removendo por completo a presunção, deu um fim ao sofrimento." </p>

<p class=Normal> Então o venerável Malunkyaputta estimulado dessa forma pelo Abençoado, levantou do seu assento e depois de homenageá-lo, mantendo-o à sua direita, partiu. </p>

<p class=Normal> Depois, permanecendo só, isolado, diligente, ardente e decidido, em pouco tempo, ele alcançou e permaneceu no objetivo supremo da vida santa pelo qual membros de um clã deixam a vida em família pela vida santa, tendo conhecido e realizado por si mesmo no aqui e agora. Ele soube: “O nascimento foi destruído, a vida santa foi vivida, o que deveria ser feito foi feito, não há mais vir a ser a nenhum estado.” E o venerável Malunkyaputta se tornou mais um dos arahants. </p>

