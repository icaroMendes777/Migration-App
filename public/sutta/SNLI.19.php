<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
eu ensinarei para vocês o poder espiritual, a base do poder espiritual, o
desenvolvimento das bases do poder espiritual, e o caminho que conduz ao
desenvolvimento das bases do poder espiritual. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que,
bhikkhus, é o poder espiritual? Neste caso, bhikkhus, um bhikkhu exerce os
vários tipos de poderes supra-humanos: tendo sido um, ele se torna vários; tendo
sido vários, ele se torna um; ele aparece e desaparece; ele cruza sem nenhum
problema uma parede, um cercado, uma montanha ou através do espaço; ele
mergulha e sai da terra como se fosse água; ele caminha sobre a água sem
afundar como se fosse terra; sentado de pernas cruzadas ele cruza o espaço como
se fosse um pássaro; com a sua mão ele toca e acaricia a lua e o sol tão forte
e poderoso; ele exerce poderes corporais até mesmo nos distantes mundos de
Brahma. Isso é chamado de poder espiritual.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que,
bhikkhus, é a base do poder espiritual? É o caminho e a prática que conduz ao
ganho do poder espiritual, à obtenção do poder espiritual. [<a href="#N1">1</a>]
<a name=R1></a>Isso é chamado a base do poder espiritual. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que,
bhikkhus, é o desenvolvimento das bases do poder espiritual? Neste caso,
bhikkhus, um bhikkhu desenvolve a base do poder espiritual que possui concentração
devido ao desejo e às formações volitivas do esforço. Ele desenvolve a base do
poder espiritual que possui concentração devido à energia e às formações
volitivas do esforço. Ele desenvolve a base do poder espiritual que possui
concentração devido à mente e às formações volitivas do esforço. Ele desenvolve
a base para do poder espiritual que possui concentração devido à investigação e
às formações volitivas do esforço. Isso é chamado o desenvolvimento das bases
do poder espiritual.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que,
bhikkhus, é o caminho que conduz ao desenvolvimento das bases do poder
espiritual? É este Nobre Caminho Óctuplo; isto é, entendimento correto, pensamento
correto, linguagem correta, ação correta, modo de vida correto, esforço
correto, atenção plena correta, concentração correta. Isso é chamado o caminho
que conduz ao desenvolvimento das bases do poder espiritual.”<o:p></o:p></span></p>

