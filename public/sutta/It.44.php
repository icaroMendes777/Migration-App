<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Notas:<o:p></o:p></span></p>


<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] Com combustível (<i style='mso-bidi-font-style:normal'>sa-upadisesa</i>)
e sem combustível restante (<i style='mso-bidi-font-style:normal'>anupadisesa</i>):
A analogia é com relação ao fogo. No primeiro caso as chamas foram extintas mas
as brasas ainda ardem. No segundo, o fogo foi extinto de tal forma que as
brasas esfriaram. O “combustível” neste caso são os cinco agregados. Enquanto o
Arahant estiver vivo, ele ainda experimentará os cinco agregados, mas eles não 
arderão mais com o fogo da cobiça, raiva e delusão. Quando o Arahant falecer, não haverá
mais nenhuma experiência dos agregados aqui ou em nenhum outro lugar. [<a
href="#R1">Retorna</a>] <o:p></o:p></span></p>


<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[2] Tal qual (<i style='mso-bidi-font-style:normal'>tadi</i>): Um
adjetivo que descreve aquele que realizou o objetivo. Indica que o estado da
pessoa é indefinível e não está sujeito à mudança ou influência de nenhum tipo.
[<a href="#R2">Retorna</a>]<o:p></o:p></span></p>



