<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] O comentário relata a história do primeiro encontro de Vangisa com o
Buda. Vangisa era um brâmane errante que ganhava o seu sustento tocando o
crânio de pessoas mortas e declarando o seu renascimento. Quando ele encontrou
o Buda, este lhe apresentou vários crânios, inclusive um dum arahant. Vangisa
foi capaz de dizer corretamente o renascimento daquelas pessoas mortas, mas
quando chegou no crânio do arahant ele ficou confuso. Ele entrou na Sangha com
o propósito de aprender a determinar o renascimento de um arahant, mas
pouco tempo depois ele abandonou essa idéia ao compreender que a vida santa é
vivida por um objetivo mais elevado. [<a href="#R1">Retorna</a>]<o:p></o:p></span></p>


