<p class=Normal> Em Savatthi. ‘Bhikkhus, eu ensinarei para vocês as coisas que agrilhoam e sobre o grilhão. Ouçam e prestem muita atenção àquilo que eu vou dizer.” – “Sim, venerável senhor,” os bhikkhus responderam. O Abençoado disse o seguinte:</p>

<p class=Normal> “E o que, bhikkhus, são coisas que agrilhoam? O que é o grilhão?</p>

<p class=Normal> “A forma é uma coisa que agrilhoa. Todo desejo ou cobiça, a ela relacionada, é o grilhão em relação a ela. O som é uma coisa que agrilhoa. Todo desejo ou cobiça, a ele relacionado, é o grilhão em relação a ele ... O objeto mental é uma coisa que agrilhoa. Todo desejo ou cobiça, a ele relacionado, é o grilhão em relação a ele. Essas são coisas que agrilhoam e esse é o grilhão.</p>

