<p class=Normal>"Bhikkhus, há esses dois tipos de grupos. Quais dois? O grupo treinado na conversa vã, não na investigação, e o grupo treinado na investigação, não na conversa vã. </p>

<p class=Normal>"E qual é o grupo treinado na conversa vã, não na investigação? Aqui, neste tipo de grupo, quando os discursos ditos pelo Tathagata estão sendo recitados, que são profundos, profundos em significado, que transcendem o mundo, conectados com o vazio, os bhikkhus não querem ouvi-los, não dão atenção, ou aplicam as suas mentes para compreendê-los; eles não acham que esses ensinamentos devem ser estudados e aprendidos. Mas quando outros discursos estão sendo recitados que são mera poesia composta por poetas, belos em palavras e frases, criados por forasteiros, ditos por discípulos, eles querem ouvi-los, lhes dão atenção, e aplicam as suas mentes para compreendê-los; eles pensam que esses ensinamentos deve ser estudados e aprendidos. E tendo aprendido esses ensinamentos, eles não se questionam uns aos outros ou examinam cuidadosamente, [perguntando]: 'Como é isso? Qual é o significado disso' Eles não revelam [aos demais] aquilo que está obscuro e elucidam o que não está claro, e não dissipam a sua perplexidade sobre numerosos pontos desconcertantes. Isso é chamado o grupo treinado na conversa vã, não na investigação. </p>

<p class=Normal>"E qual é o grupo treinado na investigação, não na conversa vã? Aqui, neste tipo de grupo, quando os discursos que estão sendo recitados são mera poesia composta por poetas, belos em palavras e frases, criados por forasteiros, ditos por discípulos, eles não querem ouvi-los, nem lhes dão atenção, e não aplicam as suas mentes para compreendê-los; eles não pensam que esses ensinamentos deve ser estudados e aprendidos. Mas quando os discursos ditos pelo Tathagata estão sendo recitados, que são profundos, profundos em significado, que transcendem o mundo, conectados com o vazio, os bhikkhus querem ouvi-los, lhes dão atenção, aplicam as suas mentes para compreendê-los; eles acham que esses ensinamentos devem ser estudados e aprendidos. E tendo aprendido esses ensinamentos, eles se questionam uns aos outros ou examinam cuidadosamente, [perguntando]: 'Como é isso? Qual é o significado disso' Eles revelam [aos demais] aquilo que está obscuro e elucidam o que não está claro, e dissipam a sua perplexidade sobre numerosos pontos desconcertantes. Isso é chamado o grupo treinado na investigação, não na conversa vã. </p>

<p class=Normal>"Esses bhikkhus, são os dois tipos de grupos. Destes dois tipos de grupos, o grupo treinado investigação, não na conversa vã, é superior." </p>

