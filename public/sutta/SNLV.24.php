<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Notas:<o:p></o:p></span></b></p>



<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] Esta seria uma desobediência ao quinto preceito. Os Sakyas pensavam
que se Sarakani violava um dos preceitos ele estaria carente do quarto fator para
entrar na correnteza, (a virtude), e dessa forma não poderia ser um dos que entrou na
correnteza. [<a href="#R1">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[2] Esta é a caracterização de um arahant, que na verdade está livre de
qualquer renascimento futuro. “Alegre sabedoria”, <i>hasapañña,</i><span
style='mso-bidi-font-style:italic'> dá-se</span> quando as etapas do
treinamento são desenvolvidas com alegria, deleite, inspiração e satisfação.
“Ágil sabedoria”, <i>javanapañña, </i>quando a compreensão dos cinco agregados
como impermanentes, sofrimento e não-eu ocorre com rapidez. [<a href="#R2">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[3] Este é o discípulo do Dhamma, (<i>dhammanusari</i>); o parágrafo
seguinte descreve o discípulo pela fé, (<i>saddhanusari</i>).
De acordo com o <a href="SNXXV.1.php">SN XXV.1</a>, estes dois tipos de
indivíduos alcançaram o plano dos nobres, mas ainda não realizaram o fruto do
entrar na correnteza. Além desses dois, há uma classificação dos nobres discípulos 
em sete tipos que pode ser encontrada no <a href="MN70.php">MN 70</a>. [<a href="#R3">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[4] No momento da morte, ele havia realizado os três treinamentos,
(virtude, concentração e sabedoria). Isso indica que, apesar dele haver se
entregado antes à bebida, através do treinamento, antes de morrer, ele entrou
na correnteza. [<a href="#R4">Retorna</a>]<o:p></o:p></span></p>






