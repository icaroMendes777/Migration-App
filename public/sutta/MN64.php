<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim ouvi.
Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no Parque de
Anathapindika. Lá ele se dirigiu aos bhikkhus: “Bhikkhus.” – “Venerável
senhor,” eles responderam. O Abençoado disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><a name=T2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2. “Bhikkhus, vocês se recordam dos cinco primeiros grilhões tal como
ensinado por mim?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isso
foi dito, o venerável Malunkyaputta respondeu: “Venerável senhor, eu me recordo
dos cinco primeiros grilhões tal como ensinado pelo Abençoado.” [</span><a
href="#N1"><span lang=PT-BR style='mso-ansi-language:PT-BR'>1</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Mas, Malunkyaputta, de que forma você se recorda dos cinco primeiros
grilhões tal como ensinado por mim?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, eu me recordo da idéia da existência de um eu como um grilhão ensinado
pelo Abençoado. Eu me recordo da dúvida como um grilhão ensinado pelo
Abençoado. Eu me recordo do apego a preceitos e rituais como um grilhão
ensinado pelo Abençoado. Eu me recordo do desejo sensual como um grilhão
ensinado pelo Abençoado. Eu me recordo da má vontade como um grilhão ensinado
pelo Abençoado. É dessa forma, venerável senhor, que eu me recordo dos cinco
grilhões tal como ensinado pelo Abençoado.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3.
“Malunkyaputta, você se recorda para quem que eu ensinei esses cinco primeiros
grilhões dessa forma? [</span><a href="#N2"><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>] <a
name=R2></a>Os errantes de outras seitas não lhe refutariam com o símile do
bebê? Pois um bebê recém-nascido não tem nem mesmo a noção de ‘identidade,’
então como a idéia da existência de um eu pode surgir nele? No entanto, a
tendência subjacente à idéia da existência de um eu está nele. [</span><a
href="#N3"><span lang=PT-BR style='mso-ansi-language:PT-BR'>3</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R3></a>Um bebê recém-nascido
não tem nem mesmo a noção de ‘ensinamentos,’[</span><a href="#N4"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>4</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>] <a name=R4></a>então como a dúvida com
respeito aos ensinamentos pode surgir nele? No entanto, a tendência subjacente
à dúvida está nele. Um bebê recém-nascido não tem nem mesmo a noção de ‘regras,’
então como o apego a preceitos e rituais pode surgir nele? No entanto, a
tendência subjacente ao apego a preceitos e rituais está nele. Um bebê
recém-nascido não tem nem mesmo a noção de ‘prazeres sensuais,’ então como o
desejo sensual pode surgir nele? No entanto, a tendência subjacente ao desejo
sensual está nele. Um bebê recém-nascido não tem nem mesmo a noção de ‘seres,’
então como a má vontade em relação aos seres pode surgir nele? No entanto, a
tendência subjacente à má vontade está nele. Os errantes de outras seitas não
lhe refutariam com este símile do bebê?” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. Em vista
disso, o venerável Ananda disse: “Agora é o momento, Abençoado, agora é o
momento, Iluminado, para que o Abençoado ensine os cinco primeiros grilhões.
Tendo ouvido do Abençoado os bhikkhus o recordarão.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então
ouça, Ananda e preste muita atenção àquilo que eu vou dizer,” “Sim, venerável
senhor,” o venerável Ananda respondeu. O Abençoado disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. “Aqui,
Ananda, uma pessoa comum sem instrução que não respeita os nobres, que não é
proficiente nem treinada no Dhamma deles, que não respeita os homens
verdadeiros, que não é proficiente nem treinada no Dhamma deles, permanece
com a mente obcecada e escravizada pela idéia da existência de um eu e ela não
compreende como na verdade é a escapatória da idéia da existência de um eu que
já surgiu; e quando essa idéia da existência de um eu se torna habitual nela e
não está subjugada, isso é um grilhão. Ela permanece com a mente obcecada e
escravizada pela dúvida … pelo apego a preceitos e rituais … pelo desejo
sensual … pela má vontade, e ela não compreende como na verdade é a escapatória
da má vontade que já surgiu; e quando essa idéia da má vontade se torna
habitual nela e não está subjugada, isso é um grilhão.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. “Um
discípulo nobre bem instruído que respeita os nobres que é proficiente e
treinado no Dhamma deles, que respeita os homens verdadeiros, que é
proficiente e treinado no Dhamma deles, não permanece com a mente obcecada
e escravizada pela idéia da existência de um eu;<span style="mso-spacerun:
yes">  </span>ele compreende como na verdade é a escapatória da idéia da
existência de um eu que já surgiu, e a idéia da existência de um eu juntamente
com a tendência subjacente são abandonadas por ele. [</span><a href="#N5"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>5</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>] <a name=R5></a>Ele não permanece com a mente
obcecada e escravizada pela dúvida ... pelo apego a preceitos e rituais … pela
desejo sensual … pela má vontade; ele compreende como na verdade é a
escapatória da má vontade que já surgiu, e a má vontade juntamente com a
tendência subjacente são abandonadas por ele.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. “Existe um
caminho, Ananda, uma prática para o abandono dos cinco primeiros grilhões; mas
alguém que não venha para esse caminho, para essa prática, possa compreender ou
ver, ou abandonar esses cinco primeiros grilhões – isso não é possível. É como
se houvesse uma grande árvore com seu cerne, não seria possível que alguém
pudesse cortar o cerne sem cortar a casca e o alburno, da mesma forma, existe
um caminho … isso não é possível.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Existe um
caminho, Ananda, uma prática para o abandono dos cinco primeiros grilhões; e
alguém que venha para esse caminho, para essa prática, possa compreender ou
ver, ou abandonar esses cinco primeiros grilhões – isso é possível. É como se
houvesse uma grande árvore com seu cerne, seria possível que alguém pudesse
cortar o cerne cortando a casca e o alburno, da mesma forma, existe um caminho
… isso é possível.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8.
“Suponha, Ananda, que o rio Ganges estivesse cheio d’água até a borda de forma
que corvos pudessem beber nele e então, um homem fraco viesse e pensasse:
‘Nadando através da correnteza com os meus braços, cruzarei com segurança até a
outra margem deste rio Ganges’; no entanto, ele não seria capaz de cruzar até a
outra margem com segurança. Da mesma forma, quando o Dhamma está sendo ensinado
para alguém, para a cessação da idéia da existência de um eu, se a mente dele
não entrar nisso e adquirir confiança, se estabilizar e se libertar, ele será
considerado como o homem fraco.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Suponha,
Ananda, que o rio Ganges estivesse cheio d´água até a borda de forma que corvos
pudessem beber nele e então, um homem forte viesse e pensasse: ‘Nadando através
da correnteza com os meus braços, cruzarei com segurança até a outra margem
deste rio Ganges’; e ele seria capaz de cruzar até a outra margem com
segurança. Da mesma forma, quando o Dhamma está sendo ensinado para alguém,
para a cessação da idéia da existência de um eu, se a mente dele entrar nisso e
adquirir confiança, se estabilizar e se libertar, então ele será considerado como o
homem forte.<o:p></o:p></span></p>



<p class=Normal><a name=T9></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>9. “E qual, Ananda, é o caminho, a prática, para o abandono dos cinco
primeiros grilhões? Aqui, um bhikkhu afastado das aquisições, [</span><a href="#N6"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>6</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>] <a name=R6></a>com o abandono dos estados
prejudiciais, com a completa tranqüilização da resistência corporal, afastado dos prazeres sensuais, 
afastado das qualidades não hábeis, entra e permanece no primeiro jhana, que é caracterizado pelo pensamento 
aplicado e sustentado, com o êxtase e felicidade nascidos do afastamento.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Tudo que
ali existe de forma material, sensação, percepção, formações volitivas e consciência, ele
vê esses estados como impermanentes, como sofrimento, como uma enfermidade, um
tumor, uma flecha, uma calamidade, uma aflição, estranhos, desintegrando,
vazios, como não-eu. [</span><a href="#N7"><span lang=PT-BR style='mso-ansi-language:
PT-BR'>7</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>] <a
name=R7></a>Ele afasta a sua mente desses estados e a dirige para o elemento
imortal, desta forma: ‘Isto é a paz, isto é o sublime, isto é, o silenciar de
todas as formações, o abandono de todas as aquisições, a destruição do desejo,
desapego, cessação, Nibbana.’ [</span><a href="#N8"><span lang=PT-BR
style='mso-ansi-language:PT-BR'>8</span></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>] <a name=R8></a>Firmando-se sobre isso, ele realiza a destruição das
impurezas. Mas se ele não realizar a destruição das impurezas, então devido a
esse desejo pelo Dhamma, esse deleite com o Dhamma, [</span><a href="#N9"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>9</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>] <a name=R9></a>com a destruição dos cinco
primeiros grilhões ele se tornará um dos que irá renascer de forma espontânea
nas Moradas Puras e lá realizará o parinibbana sem nunca mais retornar desse
mundo. Esse é o caminho, a prática para o abandono dos cinco primeiros
grilhões.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10-12.
“Além disso, abandonando o pensamento aplicado e sustentado, um bhikkhu entra e
permanece no segundo jhana ... Além disso, abandonando o êxtase um
bhikkhu ... entra e permanece no terceiro jhana ... Além disso, com o completo desaparecimento da felicidade, 
um bhikkhu entra e permanece no quarto jhana, que possui nem felicidade nem sofrimento, com a atenção plena e
 a equanimidade purificadas. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Tudo que
ali existe de forma material, sensação, percepção, formações volitivas e consciência, ele
vê esses estados como impermanentes ... como não-eu. Ele afasta a sua mente
desses estados e a dirige para o elemento imortal ... Esse é o caminho, a
prática para o abandono dos cinco primeiros grilhões.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. “Além
disso, com a completa superação das percepções da forma, com o desaparecimento
das percepções do contato sensorial, sem dar atenção às percepções da diversidade,
consciente que o ‘espaço é infinito,’ um bhikkhu entra e permanece na base do
espaço infinito.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Tudo que
ali existe de sensação, percepção, formações volitivas e consciência, [</span><a
href="#N10"><span lang=PT-BR style='mso-ansi-language:PT-BR'>10</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R10></a>ele vê esses
estados como impermanentes ... como não-eu.<span style="mso-spacerun: yes"> 
</span>Ele afasta a sua mente desses estados e a dirige para o elemento imortal
... Esse é o caminho, a prática para o abandono dos cinco primeiros grilhões.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14. “Além
disso, com a completa superação da base do espaço infinito, consciente de que a
‘consciência é infinita,’ um bhikkhu entra e permanece na base da consciência
infinita.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Tudo que
ali existe de sensação, percepção, formações volitivas e consciência, ele vê esses
estados como impermanentes ... como não-eu.<span style="mso-spacerun: yes"> 
</span>Ele afasta a sua mente desses estados e a dirige para o elemento imortal
... Esse é o caminho, a prática para o abandono dos cinco primeiros grilhões.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. “Além
disso, com a completa superação da base da consciência infinita, consciente de
que ‘não há nada,’ um bhikkhu entra e permanece na base do nada.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Tudo que
ali existe de sensação, percepção, formações volitivas e consciência, ele vê esses
estados como impermanentes, como sofrimento, como uma enfermidade, um tumor,
uma flecha, uma calamidade, uma aflição, estranhos, desintegrando, vazios, como
não-eu. Ele afasta a sua mente desses estados e a dirige para o elemento
imortal desta forma: ‘Isto é a paz, isto é o sublime, isto é, o silenciar de
todas as formações, o abandono de todas as aquisições, a destruição do desejo,
desapego, cessação, Nibbana.’ Firmando-se sobre isso, ele realiza a destruição
das impurezas. Mas se ele não realizar a destruição das impurezas, então devido
a esse desejo pelo Dhamma, esse deleite com o Dhamma, com a destruição dos
cinco primeiros grilhões ele se tornará um dos que irá renascer de forma
espontânea nas Moradas Puras e lá realizará o parinibbana sem nunca mais
retornar desse mundo. Esse é o caminho, a prática para o abandono dos cinco
primeiros grilhões.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16.
“Venerável senhor. Se esse é o caminho, a prática para o abandono dos cinco
primeiros grilhões, então como é que alguns bhikkhus, dizem, obtiveram a
libertação da mente e alguns, dizem, obtiveram a libertação através da sabedoria?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“A
diferença, Ananda, está nas faculdades deles, eu digo.” [</span><a href="#N11"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>11</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>] <a name=R11></a>Isso foi o que disse o
Abençoado. O venerável Ananda ficou satisfeito e contente com as palavras do
Abençoado<o:p></o:p></span></p>


