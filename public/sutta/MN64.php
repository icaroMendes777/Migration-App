<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[2] MA: A pergunta surge: “Quando o Buda perguntou sobre os grilhões e o
bhikkhu respondeu se referindo aos grilhões, porque o Buda criticou a sua
resposta?” A razão é que Malunkyaputta tinha a idéia de que uma pessoa é
aprisionada pelos grilhões apenas naquelas ocasiões em que estes a atacam,
enquanto que em outras ocasiões ela não estaria aprisionada por eles. O Buda
falou daquela forma para mostrar o erro nesse entendimento. [</span><a
href="#R2"><span lang=PT-BR style='mso-ansi-language:PT-BR'>Retorna</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=N3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[3] <i>Anuseti tvev'assa sakkayaditthanusayo</i>. Com respeito aos <i>anusayas</i>
ou tendências subjacentes, veja o </span><a href="MN44.php#N15"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>MN 44, nota 15</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>. Nos comentários as impurezas são
identificadas como ocorrendo em três níveis: nível <i>anusaya, </i>no qual elas
permanecem como inclinações latentes na mente; nível <i>pariyutthana, </i>onde
elas surgem para obcecar e escravizar a mente (mencionado no verso 5 deste
sutta); e nível <i>vitikkama</i>, onde<i> </i>elas causam as ações corporais e
verbais prejudiciais. O ponto da crítica do Buda é que os grilhões, mesmo
quando não se convertem em manifestação ativa, continuam a existir no nível <i>anusaya</i>
enquanto não forem erradicados pelos caminhos supramundanos. [</span><a
href="#R3"><span lang=PT-BR style='mso-ansi-language:PT-BR'>Retorna</span></a><span
