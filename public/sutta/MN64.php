<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] Os cinco primeiros grilhões, (<i>orambhagiyani samyojanani</i>), são também
chamados grilhões inferiores porque levam ao renascimento no reino da esfera
sensual. Eles são erradicados de forma completa apenas pelo ‘que não retorna.’
[</span><a href="#R1"><span lang=PT-BR style='mso-ansi-language:PT-BR'>Retorna</span></a><span
