<p class=Normal><a name=R8></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>17. “Potthapada, a partir do momento em que um bhikkhu alcançou esse
controle da percepção, [<a href="#N9">9</a>] <a name=R9></a>ele prossegue
através de passos atentos sucessivos até que alcance o pico da percepção.
Quando ele alcança o pico da percepção o pensamento lhe ocorre: ‘Pensar é ruim
para mim, não pensar é melhor. Se eu pensar e querer, essa percepção cessaria,
e uma percepção mais grosseira surgiria. E se eu não pensasse nem quisesse?’ [<a
href="#N10">10</a>] <a name=R10></a>Assim ele não pensa nem quer, e enquanto
ele não pensa nem quer, aquela percepção cessa e outra percepção grosseira não
surge. Ele alcança a cessação. E assim, Potthapada, é a forma como ocorre
através de passos atentos sucessivos a realização da cessação última da
percepção.<span style='mso-spacerun:yes'>  </span><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18. “O que
você pensa Potthapada? Você já havia ouvido isso antes?” “Não, venerável senhor. Como eu
entendo, o Abençoado disse: ‘Potthapada, a partir do momento em que um bhikkhu
alcançou esse controle da percepção, ele prossegue através de passos atentos
sucessivos até que alcance o pico da percepção … Ele alcança a cessação… E
assim é a forma como ocorre através de passos atentos sucessivos a realização
da cessação última da percepção.’” “Isso é correto, Potthapada.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>19. “Mas
venerável senhor, o Abençoado descreve apenas um pico da percepção ou vários?”<br> “Eu
descrevo um e vários picos da percepção.”<br> “E como o Abençoado descreve um e
vários?”<br> “De acordo com o modo como ele alcança a cessação, Potthapada, desse
modo eu descrevo o pico da percepção. [<a href="#N11">11</a>] <a name=R11></a>Assim
é como descrevo um pico da percepção e vários picos da percepção.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>20. “Agora,
venerável senhor, a percepção surge primeiro e o conhecimento depois, ou o conhecimento
surge primeiro e a percepção depois, ou a percepção e o conhecimento surgem
simultaneamente?”<br> “Potthapada, a percepção surge primeiro, depois o
conhecimento. E o surgimento do conhecimento ocorre do surgimento da percepção.
Ele compreende que: ‘É na dependência disso [<a href="#N12">12</a>] <a
name=R12></a>que surgiu o meu conhecimento.’ Dessa forma é possível ver que a
percepção surge primeiro, e o conhecimento depois, e o surgimento do
conhecimento ocorre do surgimento da percepção.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>21. “Agora,
venerável senhor, a percepção é o eu da pessoa, ou a percepção é uma coisa e o eu outra?”<br>
“Bem, Potthapada, qual eu você afirma?”<br> “Senhor, eu afirmo um eu grosseiro,
possuído de forma, composto dos quatro elementos e que se alimenta de comida.”<br>
“Mas com tal eu grosseiro, Potthapada, então para você a percepção seria uma
coisa e o eu outra. Você pode ver isso da seguinte forma: mesmo enquanto esse
