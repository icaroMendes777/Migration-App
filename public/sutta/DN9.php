<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14. “E
depois, com a completa superação das percepções da forma, com o desaparecimento
das percepções do contato sensorial, sem dar atenção às percepções da
diversidade, consciente de que o ‘espaço é infinito,’ ele entra e permanece na
base do espaço infinito. A sua verdadeira e refinada percepção da atenção plena e a equanimidade purificadas 
anterior cessa, e nesse momento está presente a verdadeira e refinada percepção da dimensão do espaço infinito.<span
style='mso-spacerun:yes'>  </span>Nessa ocasião ele está perceptivo a essa verdadeira e refinada
dimensão do espaço infinito. Dessa forma algumas percepções
surgem através do treinamento e algumas cessam através do treinamento.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. “E
depois, com a completa superação da base do espaço infinito, consciente de que
a ‘consciência é infinita,’ ele entra e permanece na base da consciência
infinita. A sua verdadeira e refinada percepção da dimensão do espaço
infinito cessa, e nesse momento está presente a verdadeira e refinada percepção da dimensão da consciência infinita.<span style='mso-spacerun:yes'> 
</span>Nessa ocasião ele está perceptivo a essa verdadeira e refinada dimensão da
consciência infinita. Dessa forma algumas percepções surgem através do
