<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16. “E
depois, com a completa superação da base da consciência infinita, consciente de
que ‘não há nada,’ ele entra e permanece na base do nada. A sua verdadeira e refinada percepção da dimensão da consciência infinita cessa, e nesse
momento está presente a verdadeira e refinada percepção da dimensão do
nada.<span style='mso-spacerun:yes'>  </span>Nessa ocasião ele está perceptivo
a essa verdadeira e refinada dimensão do nada. Dessa forma algumas percepções
surgem através do treinamento e algumas cessam através do treinamento. [<a
