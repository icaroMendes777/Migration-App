e algumas cessam através do treinamento.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><a name=T12></a>12. “E
depois, abandonando o êxtase, um bhikkhu entra e permanece no terceiro jhana 
que é caracterizado pela felicidade sem o êxtase, acompanhada pela atenção plena, 
