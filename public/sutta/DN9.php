<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>23.
“Venerável senhor, eu afirmo um eu sem forma feito de percepção.” [<a href="#N15">15</a>]
<a name=R15></a><br>“Mas com tal eu sem forma feito de percepção, Potthapada, então
