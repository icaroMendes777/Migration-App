<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Potthapada
respondeu: “Eu também não entendo se o mundo é eterno ou não ... ou se o
Tathagata existe após a morte ou não, ou ambos, ou nenhuma delas. Mas o
contemplativo Gotama ensina uma forma de prática real e verdadeira que está de
acordo com o Dhamma e fundamentada no Dhamma. E por que um homem como eu não
deveria expressar aprovação por uma prática real e verdadeira como essa, tão
bem ensinada pelo contemplativo Gotama?” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>32. Dois ou
três dias depois, Citta, o filho do treinador de elefantes, foi com Potthapada
ver o Abençoado. Citta homenageou o Abençoado e sentou a um lado. Potthapada
cumprimentou o Abençoado, sentou a um lado e contou o que aconteceu.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>33. “Potthapada, todos esses errantes são cegos e sem visão, você é o
único entre eles que possui visão. Algumas coisas eu ensinei e apontei,
Potthapada, como sendo definidas, outras como sendo indefinidas. Quais coisas
apontei como indefinidas? ‘O mundo é eterno’ declarei como sendo indefinido ...’ O
Tathagata existe após a morte ...’ Por que? Porque elas não trazem benefício ... a
Nibbana. É por isso que eu as declarei como indefinidas. <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Mas, quais coisas eu apontei como definidas? ‘Isto é sofrimento, esta é
a origem do sofrimento, esta é a cessação do sofrimento e este é o caminho que
conduz à cessação do sofrimento.’ Por que? Porque elas trazem benefício,
pertencem aos fundamentos da vida santa, conduzem ao desencantamento, ao
desapego, à cessação, à paz, ao conhecimento direto, à iluminação, a Nibbana. É
por isso que eu as declarei como definidas. <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>34. “Potthapada, há alguns contemplativos e brâmanes que declaram e
acreditam que após a morte o eu fica completamente feliz e livre de
enfermidades. Eu fui até eles e perguntei se de fato era isso que eles
declaravam e acreditavam, e eles responderam: ‘Sim.’ Então eu disse: ‘Vocês,
amigos, vivendo no mundo, o conhecem e vêm como um lugar plenamente feliz?’ E
eles responderam: ‘Não’. Eu disse: ‘Vocês já experimentaram uma noite ou dia ou
metade de uma noite ou dia que fosse, completamente feliz?’ e eles responderam:
‘Não.’ Eu disse: ‘Vocês conhecem um caminho ou uma prática através da qual um
mundo completamente feliz possa ser produzido?’ e eles responderam: ‘Não.’ Eu
disse: ‘Vocês ouviram as vozes de divindades que renasceram num mundo
completamente feliz, dizendo: “Pratiquem bem, estimados senhores. Pratiquem com
diligência, estimados senhores, para alcançar um mundo completamente feliz,
porque foi através dessa prática que nós mesmos renascemos neste mundo
completamente feliz”?’ e eles responderam: ‘Não.’ O que você pensa Potthapada?
Sendo esse o caso, a conversa daqueles contemplativos e brâmanes não seriam
apenas tolices?” “Sim, venerável senhor, sendo esse o caso, a conversa daqueles
contemplativos e brâmanes seriam apenas tolices.”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>35. “É o mesmo que se um homem dissesse: ‘Eu estou apaixonado pela moça
mais bonita deste pais.’ Então lhe perguntariam: ‘Bom homem, essa moça mais
bonita deste pais pela qual você está apaixonado – você sabe se ela é da classe
nobre ou da classe dos brâmanes ou da classe dos comerciantes ou da classe dos
trabalhadores?’ e ele responderia: ‘Não.’ Então lhe perguntariam: ‘Bom homem,
essa moça mais bonita deste pais com a qual você está apaixonado – você sabe o
nome e o clã dela? ... Se ela é alta ou baixa ou com estatura média?... Se ela tem a
complexão escura, clara ou dourada?... Em qual vilarejo, vila ou cidade ela vive?’
e ele responderia: ‘Não.’ E então lhe perguntariam: ‘Bom homem, você então está
apaixonado por uma moça que você nem conhece ou viu?’ e ele responderia: ‘Sim.’
O que você pensa, Potthapada, em sendo assim, a conversa daquele homem não
seria apenas tolice?” “Sim, venerável senhor, </span>a conversa<span lang=PT-BR
style='mso-ansi-language:PT-BR'> ... apenas tolice.”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>36. “E assim também é com esses contemplativos e brâmanes que declaram e
acreditam que após a morte o eu fica completamente feliz e livre de
enfermidades ... a conversa daqueles contemplativos e brâmanes não seriam
apenas tolices?” - “Sim, venerável senhor, sendo ...<span style='mso-spacerun:yes'> 
</span>apenas tolices.”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>37. “É como se um homem fosse construir uma escadaria para um palácio
numa encruzilhada. As pessoas poderiam dizer: ‘Essa escadaria para o palácio –
você sabe se a frente do palácio estará para o leste ou oeste, norte ou sul ou
se o palácio será alto, baixo ou médio?’ e ele diria: ‘Não.’ E elas poderiam dizer:
‘Bem então, você não sabe ou imagina para que tipo de palácio está construindo
a escadaria?’ e ele responderia: ‘Não.’ A conversa daquele homem não seria
apenas tolice?” - “Sim, venerável senhor, a conversa ...<span style='mso-spacerun:yes'> 
</span>apenas tolice.”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>38. (Igual ao verso 34)<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>39. “Potthapada, há essas três aquisições de um eu: [<a href="#N17">17</a>]
<a name=R17></a>o eu grosseiro, o eu feito pela mente, o eu adquirido sem
forma. O que é o eu grosseiro? Possui forma, composto dos quatro grandes
elementos, alimentado por comida. O que é o eu feito pela mente? Possui forma,
completo com todas as suas partes, sem defeito em nenhuma das faculdades. O que
é o eu sem forma? Sem forma e feito de percepção.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>40. “Eu ensino o Dhamma para o abandono do eu grosseiro, de tal modo que
ao praticá-lo, as qualidades mentais impuras sejam abandonadas e as qualidades
mentais luminosas se fortaleçam, e você entre e permaneça na culminação e
perfeição da sabedoria aqui e agora, tendo realizado e alcançado isso por você
mesmo. Se o pensamento lhe ocorrer que quando as qualidades mentais impuras são
abandonadas e as qualidades mentais luminosas se fortalecem, e você entrar e
permanecer na culminação e perfeição da sabedoria aqui e agora, tendo realizado
e alcançado isso por você mesmo, você estará permanecendo com o sofrimento,
isso não deve ser visto dessa forma. [<a href="#N18">18</a>] <a name=R18></a>Quando
as qualidades mentais impuras são abandonadas e as qualidades mentais luminosas
se fortalecem, e você entra e permanece na culminação e perfeição da sabedoria
aqui e agora, tendo realizado e alcançado isso por você mesmo, não há nada além
de felicidade, êxtase, tranqüilidade, atenção plena e plena consciência, e uma
permanência feliz e prazerosa.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>41. “Eu
também ensino o Dhamma para o abandono do eu feito pela mente ... (igual ao verso
40).<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>42. “Eu
também ensino o Dhamma para o abandono do eu sem forma ... (igual ao verso 40).<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>43. “No
passado fui perguntado: ‘O que amigo é esse eu grosseiro para cujo abandono
você ensina o Dhamma, de tal modo que ao praticá-lo, as qualidades mentais
impuras sejam abandonadas e as qualidades mentais luminosas se fortaleçam, e
você entre e permaneça na culminação e perfeição da sabedoria aqui e agora,
tendo realizado e alcançado isso por você mesmo?’ Sendo perguntado dessa forma,
eu respondi: ‘<i>Esse</i> <i>é</i><i style='mso-bidi-font-style:normal'> </i>[<a
href="#N19">19</a>] <a name=R19></a>o eu grosseiro, para cujo abandono ensino o
Dhamma ...’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>44. “No
passado fui perguntado: ‘O que amigo é esse eu feito pela mente ...?’” (igual ao
verso 43).<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>45. “No
passado fui perguntado: ‘O que amigo é esse eu sem forma ...?’ (igual ao verso
43). O que você pensa Potthapada? Esse enunciado não se torna bem
fundamentado?” - “Certamente, venerável senhor, esse enunciado se torna bem fundamentado.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>46. “É o
mesmo que se um homem construísse uma escadaria para um palácio que estivesse
abaixo daquele palácio. As pessoas poderiam lhe perguntar: ‘E agora, essa
escadaria que você está construindo para um palácio, você sabe se o palácio
estará de frente para o leste, ou oeste, ou norte, ou sul, ou se será alto,
baixo ou médio?’ e ele diria: ‘Essa escadaria está exatamente embaixo do
palácio.’ Você não pensa que a afirmação desse homem estaria bem fundamentada?” -
“Certamente, venerável senhor, essa afirmação estaria bem fundamentada.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>47. “Da
mesma forma, no passado fui perguntado: ‘O que é esse eu grosseiro ...?’ ‘O que é
esse eu feito pela mente ...?’ ‘O que é esse eu sem forma ...?’ eu respondi: ‘<i>Esse</i>
<i style='mso-bidi-font-style:normal'>é </i>o eu adquirido (grosseiro, feito
pela mente, sem forma) para cujo abandono ensino o Dhamma, de tal modo que ao
praticá-lo, as qualidades mentais impuras sejam abandonadas e as qualidades
mentais luminosas se fortaleçam, e você entre e permaneça na culminação e
perfeição da sabedoria aqui e agora, tendo realizado e alcançado isso por você
mesmo.’ Você não pensa que essa afirmação estaria bem fundamentada?” -
“Certamente, venerável senhor, essa afirmação estaria bem fundamentada.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>48. Com
isso, Citta, o filho do treinador de elefantes, disse ao Abençoado: “Venerável senhor,
sempre que o eu grosseiro está presente, seria errado assumir a existência do
eu feito pela mente, ou do eu sem forma? Então somente o eu grosseiro é
verdadeiro? E assim com o eu feito pela mente e o eu sem forma?”<span
style='mso-spacerun:yes'>  </span><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>49. “Citta,
sempre que o eu grosseiro está presente, este não é classificado quer seja como
um eu feito pela mente nem é classificado como um eu sem forma. É classificado
apenas como um eu grosseiro. [<a href="#N20">20</a>] <a name=R20></a>Sempre que
o eu feito pela mente está presente, este não é classificado quer seja como um
eu grosseiro nem como um eu sem forma. É classificado apenas como um eu feito
pela mente. Sempre que o eu sem forma está presente, este não é classificado
quer seja como um eu grosseiro nem como um eu feito pela mente. É classificado
apenas como um eu sem forma.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Citta,
suponha que lhe perguntem: ‘Você existiu no passado ou você não existiu, você
existirá no futuro ou você não existirá?’ como você responderia?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável senhor, se
me perguntassem isso, eu responderia: ‘Eu existi no passado, eu não não existi;
eu existirei no futuro, eu não não existirei; eu existo agora, eu não não
existo.’ Assim, Senhor, seria a minha resposta.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>50. “Mas,
Citta, se eles perguntassem: ‘O eu que você tinha no passado, essa foi a sua
única aquisição verdadeira de um eu, e a do futuro e a do presente são falsas?
Ou é aquela que você terá no futuro a sua única aquisição verdadeira de um eu,
e a do passado e a do presente são falsos? Ou o eu adquirido no presente é o
único verdadeiro, e as aquisições do passado e do futuro são falsas?’ como você
responderia?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável senhor, se
eles me perguntassem isso, eu responderia: ‘Meu eu adquirido no passado era
naquela ocasião a única verdadeira aquisição de um eu, as do futuro e do
presente eram falsas. O meu eu adquirido no futuro será então a única
verdadeira aquisição de um eu, as do passado e do presente serão falsas. Meu eu
adquirido no presente é agora a única verdadeira aquisição de um eu, as do
passado e do futuro são falsas.’ Assim é como eu responderia.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>51. “Da
mesma forma, Citta, sempre que o eu grosseiro está presente, este não é
classificado quer seja como um eu feito pela mente nem é classificado como um
eu sem forma ... apenas como um eu sem forma. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>52. “Da
mesma forma, Citta, da vaca obtemos o leite, do leite a coalhada, da coalhada a
manteiga, da manteiga a manteiga clarificada e da manteiga clarificada o creme da
manteiga clarificada. E quando há leite, não falamos de coalhada, de manteiga, de
manteiga clarificada, de creme da manteiga clarificada, falamos de leite; quando há
coalhada, não falamos de manteiga …; quando há creme de manteiga clarificada …
falamos de creme de manteiga clarificada. <o:p></o:p></span></p>



<p class=Normal><a name=T53></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>53. “Assim também, sempre que o eu grosseiro está presente, este não é
classificado quer seja como um eu feito pela mente nem é classificado como um
eu sem forma. É classificado apenas como um eu grosseiro. Sempre que o eu feito
pela mente está presente, este não é classificado quer seja como um eu
grosseiro nem como um eu adquirido sem forma. É classificado apenas como um eu
feito pela mente. Sempre que o eu sem forma está presente, este não é
classificado quer seja como um eu grosseiro nem como um eu feito pela mente. É
classificado apenas como um eu sem forma. Mas, Citta, esses são apenas nomes,
expressões, modos de linguagem, designações de uso corrente no mundo, que o
Tathagata usa sem se agarrar nelas.” [<a href="#N21">21</a>] <o:p></o:p></span></p>



<p class=Normal><a name=R21></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>54. Quando isso foi dito, o errante Potthapada disse para o Abençoado:
“Magnífico, Mestre Gotama! Magnífico, Mestre Gotama! Mestre Gotama esclareceu o
Dhamma de várias formas, como se tivesse colocado em pé o que estava de cabeça
para baixo, revelasse o que estava escondido, mostrasse o caminho para alguém
que estivesse perdido ou segurasse uma lâmpada no escuro para aqueles que
possuem visão pudessem ver as formas. Eu busco refúgio no Mestre Gotama, no
Dhamma e na Sangha. Que o Mestre Gotama me aceite como um discípulo leigo que
nele buscou refúgio para o resto da vida!”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>55. Mas
Citta, o filho do treinador de elefantes, disse para o Abençoado: “Magnífico,
Mestre Gotama! Magnífico, Mestre Gotama! Mestre Gotama esclareceu o Dhamma de
várias formas, como se tivesse colocado em pé o que estava de cabeça para
baixo, revelasse o que estava escondido, mostrasse o caminho para alguém que
estivesse perdido ou segurasse uma lâmpada no escuro para aqueles que possuem
visão pudessem ver as formas. Eu busco refúgio no Mestre Gotama, no Dhamma e na
Sangha. Que eu possa receber a admissão na vida santa sob o Mestre Gotama, que
eu possa receber a admissão completa!”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>56. E
Citta, o filho do treinador de elefantes, recebeu a admissão na vida santa sob
o Abençoado e a admissão completa. Permanecendo só, isolado, diligente, ardente
e decidido, em pouco tempo, o Venerável Citta alcançou e permaneceu no objetivo
supremo da vida santa pelo qual membros de um clã deixam a vida em família pela
vida santa, tendo conhecido e realizado por si mesmo no aqui e agora. Ele
soube: “O nascimento foi destruído, a vida santa foi vivida, o que deveria ser
feito foi feito, não há mais vir a ser a nenhum estado.” E assim o Venerável
Citta, filho do treinador de elefantes, tornou-se mais um dos Arahants. <a
name=R27></a><o:p></o:p></span></p>

