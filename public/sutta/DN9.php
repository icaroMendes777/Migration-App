<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião o Abençoado estava em Savathi no Bosque de Jeta, no
Parque de Anathapindika. Naquela ocasião o errante Potthapada estava no salão de
debates próximo à árvore Tinduka, no parque da rainha Mallika, [<a href="#N1">1</a>]
<a name=R1></a>com um grande grupo de cerca de trezentos errantes.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. Então,
ao amanhecer, o Abençoado se vestiu e tomando a tigela e o manto externo foi
para Savathi para esmolar alimentos. Mas ele pensou o seguinte: “Ainda é muito
cedo para ir até Savathi para esmolar alimentos. E se eu fosse até o salão de
debates para ver o errante Potthapada?” E assim ele fez.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. Lá
estava Potthapada com uma grande assembléia de errantes que estavam fazendo uma
grande baderna, conversando em voz alta e aos berros sobre muitos assuntos
inúteis, tal como falar sobre reis, ladrões, ministros de estado, exércitos,
alarmes e batalhas; comida e bebida, roupas, mobília, ornamentos e perfumes, parentes;
veículos; vilarejos, vilas, cidades, o campo; mulheres e heróis; as fofocas das
ruas e do poço; contos dos mortos; contos da diversidade (discussões
filosóficas do passado e futuro), a criação do mundo e do mar e falar sobre a
existência ou não das coisas.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. Então
Potthapada viu o Abençoado chegando à distância. Ao vê-lo, ele silenciou a
assembléia dizendo o seguinte: “Senhores, fiquem quietos; senhores, não façam
ruído. Ali vem o contemplativo Gotama. Esse venerável gosta do silêncio e
recomenda o silêncio. Talvez, se ele encontrar a nossa assembléia em silêncio,
pensará em juntar-se a nós.”<span style='mso-spacerun:yes'>  </span>Então os
errantes ficaram em silêncio. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. O
Abençoado foi até<span style='mso-spacerun:yes'>  </span>Potthapada, que lhe
disse: “Venha venerável senhor! Bem vindo venerável senhor! Já faz muito tempo desde que o
venerável senhor encontrou uma oportunidade para vir aqui. Que o venerável senhor sente; este
assento está preparado.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O Abençoado
sentou no assento que havia sido preparado e Potthapada sentou a um lado, num
assento mais baixo. O Abençoado disse: “Qual é o assunto que faz com que vocês
estejam sentados juntos aqui agora, Potthapada? E qual é a discussão que foi
interrompida?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6.
“Venerável Senhor, deixemos de lado a discussão pela qual estamos aqui sentados
juntos. O Abençoado poderá ouvi-la mais tarde. Nos últimos dias, venerável
senhor, a discussão entre os contemplativos e brâmanes de várias seitas que se
reúnem no salão de debates diz respeito à cessação última da percepção, [<a
href="#N2">2</a>] <a name=R2></a>e como esta ocorre. Alguns disseram: ‘As
percepções de uma pessoa surgem e cessam sem causa e condição. Quando elas
surgem, a pessoa está perceptiva, quando elas cessam, então ela está não
perceptiva.’ Assim é como eles explicaram. Mas um outro grupo disse: ‘Não, não
é assim. As percepções [<a href="#N3">3</a>]<a name=R3></a> são o eu de uma
pessoa, que vêm e vão. Quando vêm, ela está perceptiva, quando vão, ela está
não perceptiva.’ Um outro disse: ‘Não é assim. Existem contemplativos e
brâmanes com grandes poderes, com grande influência. Eles empurram a percepção para
dentro da pessoa e a retiram. Quando eles a empurram, ela está perceptiva,
quando eles a retiram, ela está não perceptiva.’ [<a href="#N4">4</a>] <a
name=R4></a>E um outro disse: ‘Não, não é assim. Existem divindades com grandes
poderes, com grande influência.<span style='mso-spacerun:yes'>  </span>Elas
empurram a percepção para dentro da pessoa e a retiram. Quando elas a empurram,
a pessoa está perceptiva, quando elas a retiram, a pessoa está não
perceptiva.’<span style='mso-spacerun:yes'>  </span><a name=R5></a>Foi com
relação a isso que lembrei do venerável senhor: ‘Ah, o Abençoado! Ah, o Iluminado! Que
com certeza tem grande habilidade [<a href="#N5">5</a>] <a name=R6></a>nesse
tipo de questões. O Abençoado tem grande habilidade na cessação última da
percepção.’ O que então, venerável senhor, é a cessação última da percepção?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. “Com
relação a isso, Potthapada, aqueles contemplativos e brâmanes que dizem que as
percepções de uma pessoa surgem e cessam sem causa e condição estão
completamente errados. Por que isso? As percepções de uma pessoa surgem e cessam
devido a causas e condições. Algumas percepções surgem através do treinamento e
algumas cessam através do treinamento. O que é esse treinamento? Potthapada, um
Tathagata surge no mundo, um arahant , perfeitamente iluminado, consumado no
verdadeiro conhecimento e conduta, bem-aventurado, conhecedor dos mundos, um líder
insuperável de pessoas preparadas para serem treinadas, mestre de devas e
humanos, desperto, sublime. Ele declara - tendo realizado por si próprio com o
conhecimento direto - este mundo com os seus devas, maras e brahmas, esta
população com seus contemplativos e brâmanes, seus príncipes e povo. Ele ensina
o Dhamma, com o significado e fraseado corretos, que é admirável no início,
admirável no meio, admirável no final; e ele revela uma vida santa que é
completamente perfeita e imaculada. Um discípulo segue a vida santa e pratica a
virtude<span style='mso-spacerun:yes'>  </span>(Veja o </span><a
href="DN2.php#T41"><span lang=PT-BR style='mso-ansi-language:PT-BR'>DN 2,
versos 41-62</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>). Isso
para ele é a virtude. <span style='color:red'><o:p></o:p></span></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. “E
então, Potthapada, aquele bhikkhu que<span style='mso-spacerun:yes'> 
</span>tem a virtude consumada não vê perigo em lugar nenhum ... (Veja o <a
href="DN2.php#T63">DN 2, verso 63</a>) ... Assim é como um bhikkhu é consumado em
virtude. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9-10. “Ao
ver uma forma com o olho, um bhikkhu<span style='mso-spacerun:yes'>  </span>não
se agarra aos seus sinais ou detalhes ... (veja o <a href="DN2.php#T64">DN 2,
versos 64-75</a>). Tendo alcançado o primeiro jhana ele permanece nele. E a
percepção dos sentidos que ele tinha antes cessa e nesse momento está
