equanimidade. Dessa forma algumas percepções surgem através
do treinamento e algumas cessam através do treinamento.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><a name=T13></a>13. “E
depois, com o completo desaparecimento da felicidade, um bhikkhu entra e 
permanece no quarto jhana, que possui nem felicidade nem sofrimento, com 
a atenção plena e a equanimidade purificadas. A sua verdadeira e refinada percepção de equanimidade
anterior cessa, e nesse momento está presente a verdadeira e refinada percepção da atenção plena e a equanimidade purificadas.<span style='mso-spacerun:yes'>  </span>Nessa
