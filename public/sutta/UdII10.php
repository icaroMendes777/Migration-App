<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em certa ocasião, o
Abençoado estava em <a name=anupiya>Anupiya</a> no mangueiral. Agora naquela
ocasião, o Ven. Bhaddiya Kaligodha, ao ir para a floresta,
para o pé de uma árvore, ou para uma cabana vazia, repetidamente exclamava,
“Que felicidade! Que felicidade!” Um grande número de bhikkhus ouviu o Ven. Bhaddiya
Kaligodha, ao ir para a floresta, para o pé de uma árvore, ou para uma cabana
vazia, repetidamente exclamar, “Que felicidade! Que felicidade!” e ao ouvi-lo o
pensamento lhes ocorreu, “Não há dúvida que o Ven. Bhaddiya Kaligodha não sente
prazer com a vida santa, pois quando ele era um rei, ele conhecia a felicidade
da realeza, e agora, ao se recordar daquilo, ele repetidamente
exclama, ‘Que felicidade! Que felicidade!’” Eles foram até o Abençoado e depois
de cumprimentá-lo sentaram a um lado e disseram: “O Ven. Bhaddiya Kaligodha,
senhor,<span style='mso-spacerun:yes'>  </span>ao ir para a floresta, para o pé
de uma árvore, ou para uma cabana vazia, repetidamente exclama, ‘Que
felicidade! Que felicidade!’ Não há dúvida que o Ven. Bhaddiya Kaligodha não
sente prazer com a vida santa, pois quando ele era um rei ele conhecia a
felicidade da realeza, e agora, ao se recordar daquilo, ele
repetidamente exclama, ‘Que felicidade! Que felicidade!’”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, o Abençoado disse
para um certo bhikkhu, “Venha, bhikkhu. Em meu nome, chame Bhaddiya, dizendo,
“O Mestre o chama, meu amigo.’” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim, senhor,” o bhikkhu respondeu
e tendo ido até o Ven. Bhaddiya, ao chegar ele disse, “O Mestre o chama, meu
amigo.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim, amigo,” o Ven. Bhaddiya respondeu. Então ele foi até o Abençoado e depois de
cumprimentá-lo sentou a um lado e o Abençoado disse:<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“É verdade, Bhaddiya que indo
para a floresta, para o pé de uma árvore, ou para uma cabana vazia, você
repetidamente exclama, ‘Que felicidade! Que felicidade!’?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-spacerun:yes'> </span>“Sim, senhor.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O que você tem em mente
quando repetidamente exclama,<span style='mso-spacerun:yes'>  </span>‘Que
felicidade! Que felicidade!’?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Antes, quando eu era um
rei, eu tinha guardas postados dentro e fora dos aposentos, dentro e fora da
cidade, dentro e fora da área rural. Mas mesmo estando guardado dessa forma,
protegido dessa forma, eu vivia com medo – agitado, desconfiado e amedrontado.
Porém agora, indo sozinho para a floresta, para o pé de uma árvore, ou uma
cabana vazia, eu permaneço sem medo, sem agitação, confiante e destemido –
despreocupado, calmo, minhas necessidades satisfeitas, com a minha mente como
um gamo selvagem. Isso é o que tenho em mente quando<span
style='mso-spacerun:yes'>  </span>repetidamente exclamo, ‘Que felicidade! Que
felicidade!’” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, dando-se conta do
significado disso, o Abençoado nessa ocasião exclamou: <o:p></o:p></span></p>

