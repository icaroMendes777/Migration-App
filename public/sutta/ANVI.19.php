<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em certa
ocasião o Abençoado estava em Nadika na Casa de Tijolos. Lá ele se
dirigiu aos monges desta forma: “Bhikkhus”. – “Venerável Senhor”, eles
responderam. O Abençoado disse o seguinte:<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“A atenção plena na morte,
quando desenvolvida e cultivada traz grandes frutos e benefícios. Ela mergulha
no Imortal, tem no Imortal o seu objetivo final. Então vocês devem desenvolver
a atenção plena na morte.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isso foi dito, um
certo bhikkhu se dirigiu ao Abençoado, “Eu desenvolvo a atenção plena na
morte.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como você desenvolve a
atenção plena na morte?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu penso, ‘Que eu
possa viver por um dia e uma noite, que eu possa seguir as instruções do
Abençoado. Eu teria realizado muito.’ Assim é como desenvolvo a atenção plena
na morte.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então um outro bhikkhu se
dirigiu ao Abençoado, “Eu, também
desenvolvo a atenção plena na morte.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como você desenvolve a
atenção plena na morte?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu penso, ‘Que eu
possa viver por um dia, que eu possa seguir as instruções do Abençoado. Eu
teria realizado muito.’ Assim é como desenvolvo a atenção plena na morte. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então um outro bhikkhu se
dirigiu ao Abençoado, “Eu, também
desenvolvo a atenção plena na morte.”... “Eu penso, ‘Que eu possa viver o
tempo que tarda para comer uma refeição, que eu possa seguir as instruções do
Abençoado. Eu teria realizado muito.’…” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então um outro bhikkhu se
dirigiu ao Abençoado, “Eu, também
desenvolvo a atenção plena na morte.”... “Eu penso, ‘Que eu possa viver o
tempo que tarda para engolir, depois de mastigar, quatro bocados de comida, que
eu possa seguir as instruções do Abençoado. Eu teria realizado muito.’…” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então um outro bhikkhu se
dirigiu ao Abençoado, “Eu, também
desenvolvo a atenção plena na morte.”... “Eu penso, ‘Que eu possa viver o
tempo que tarda para engolir, depois de mastigar, um bocado de comida, que eu
possa seguir as instruções do Abençoado. Eu teria realizado muito.’…”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então um outro bhikkhu se
dirigiu ao Abençoado, “Eu, também
desenvolvo a atenção plena na morte.”... “Eu penso, ‘Que eu possa viver o
tempo que tarda para expirar depois de haver inspirado ou de inspirar depois de
haver expirado, que eu possa seguir as instruções do Abençoado. Eu teria
realizado muito.’…”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isso foi dito, o
Abençoado se dirigiu aos bhikkhus: “Bhikkhus, o bhikkhu que desenvolve a atenção plena na morte assim:<span style="mso-spacerun: yes">  </span>‘que eu
possa viver por um dia e uma noite ... por um dia ... o tempo que tarda para
comer uma refeição ... o tempo que tarda para engolir, depois de mastigar,
quatro bocados de comida, que eu possa seguir as instruções do Abençoado. Eu
teria realizado muito’ – eles são chamados bhikkhus que permanecem negligentes. Eles
desenvolvem a atenção plena na morte com lentidão, com o propósito de dar um
fim às impurezas. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas o bhikkhu que desenvolve a atenção plena na morte assim:<span style="mso-spacerun: yes"> 
</span>‘que eu possa, viver o tempo que tarda para engolir, depois de
mastigar, um bocado de comida ... viver o tempo que tarda para expirar depois
de haver inspirado ou de inspirar depois de haver expirado, que eu possa seguir
as instruções do Abençoado. Eu teria realizado muito’ – eles são chamados bhikkhus que permanecem diligentes. Eles desenvolvem a atenção plena na morte de modo penetrante,
com o propósito de dar um fim às impurezas.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto, assim vocês devem
treinar: ‘Nós permaneceremos diligentes. Nós iremos desenvolver a atenção
plena na morte de modo penetrante, com o propósito de dar um fim às impurezas.’
Assim é como vocês deveriam treinar.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o que o Abençoado
disse. Os bhikkhus ficaram contentes e satisfeitos com as palavras do
Abençoado.<o:p></o:p></span></p>

