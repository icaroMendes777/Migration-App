se referem a impurezas subjetivas conectadas com a sensualidade, isto é, desejo
sensual. MA equipara, “que alguém possa se ocupar com prazeres sensuais”, com,
“que alguém desfrute de uma relação sexual”. MT diz que outros atos físicos que
expressam desejo sexual como o abraço e a carícia também devem ser incluídos. [<a
href="#R5">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[6] MA explica que esta passagem é mencionada com o objetivo de mostrar
o defeito em adquirir o conhecimento intelectual do Dhamma com a motivação
incorreta – aparentemente a armadilha na qual Arittha havia caído. O “benefício,
(<i>attha</i>), pelo qual ele aprendeu o Dhamma” são os caminhos e os frutos. [<a
href="#R6">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N7></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[7] Este
conhecido “símile da balsa” prossegue com o mesmo argumento contra o mal uso do
aprendizado apresentado pelo símile da cobra. Alguém que se preocupa em usar o
Dhamma para gerar controvérsia e ganhar discussões carrega o Dhamma sobre a
cabeça ao invés de usá-lo para cruzar a torrente. [<a href="#R7">Retorna</a>]<o:p></o:p></span></p>

<p class=Normal><a name=N8></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[8] <i>Dhamma
pi vo pahatabba pageva adhamma.</i> MA identifica os bons estados como sendo a tranqüilidade
e o insight, (<i>samatha-vipassana</i>), e parafraseia o significado: “Eu ensino,
bhikkhus, até mesmo o abandono do desejo e apego a esses estados sublimes e
cheios de paz, como a tranqüilidade e o insight, tanto mais essa coisa baixa,
vulgar, desprezível, grosseira e impura que esse tolo Arittha considera inócua,
quando diz que não existe obstrução no desejo e cobiça pelos cinco prazeres
sensuais”. O comentarista cita o <a href="MN66.php#T26">MN 66.26-33</a> como um
exemplo do Buda ensinando o abandono do apego à tranqüilidade e o <a
href="MN38.php#T14">MN 38.14</a> como um exemplo dele ensinando o abandono do
apego ao insight. Note que em cada caso o <i>apego </i>aos bons estados é que
deve ser abandonado, não os bons estados em si. A determinação do Buda não é um
convite a um niilismo moral ou uma sugestão de que a pessoa iluminada tenha
superado o bem e o mal. Com relação a isso veja o <a href="MN76.php#T51">MN
76.51</a> [<a href="#R8">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N9></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[9] Esta
seção evidentemente tem o propósito de antecipar um outro tipo de conceito
errado e deturpado do Dhamma, isto é, a introdução da idéia de um eu nos
ensinamentos. De acordo com MA, os pontos de vista, (<i>ditthitthana</i>), tanto
são idéias incorretas em si mesmas, como também base para outras idéias
incorretas mais elaboradas; os objetos das idéias, isto é, os cinco agregados;
e as condições para as idéias, isto é, tais fatores como a ignorância,
percepção distorcida, pensamentos incorretos, etc. [<a href="#R9">Retorna</a>] <o:p></o:p></span></p>


<p class=Normal><a name=N10></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[10] MA menciona
que a noção “isso é meu” é induzida pelo desejo, a noção “isso sou eu” pela
presunção e a noção “isso é o meu eu” pelas idéias incorretas. Esses três –
desejo, presunção e idéias – são denominados as três obsessões, (<i>gaha</i>). Eles
são também a causa principal por detrás da concepção (<a href="MN1.php">MN 1</a>)
e da proliferação mental (<a href="MN18.php">MN 18</a>). [<a href="#R10">Retorna</a>]<o:p></o:p></span></p>

<p class=Normal><a name=N11></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[11] MA: Esta
série de termos mostra o agregado da consciência de forma indireta, através do
seu objeto. O “visto” aponta para a consciência no olho, o “ouvido” para a
consciência no ouvido, o “sentido” para os outros três tipos de consciência nos
sentidos e o último se refere à consciência na mente. [<a href="#R11">Retorna</a>]<o:p></o:p></span></p>

<p class=Normal><a name=N12></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[12] Esta é
uma versão completa da visão do eterno que surgiu com base em uma idéia
anterior, mais rudimentar, acerca da identidade; neste caso ela se torna em si
mesma um objeto do desejo, presunção e a falsa idéia de um eu. Essa idéia parece
refletir a filosofia dos Upanishads que afirma a identidade de um eu individual, (<i>atman</i>),
com o espirito universal, (<i>brahman</i>), embora seja difícil estabelecer com
base nos textos se o Buda tinha conhecimento acerca dos Upanishads. [<a
href="#R12">Retorna</a>]<o:p></o:p></span></p>

<p class=Normal><a name=N13></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[13] <i>Asati
na paritassati. </i>O substantivo <i>paritassana </i>de acordo com MA tem a
dupla conotação de medo e desejo, dessa forma “agitação” foi escolhida como
contendo ambos. Agitação por aquilo que não existe externamente (verso 18)
refere-se ao desespero pela perda ou não aquisição de posses; agitação por
aquilo que não existe internamente (verso 20) é o desespero daquele que crê no
eterno ao interpretar equivocadamente o ensinamento do Buda acerca de Nibbana
como sendo uma doutrina de aniquilação. [<a href="#R13">Retorna</a>]<o:p></o:p></span></p>

<p class=Normal><a name=N14></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[14] <i>Pariggaham
pariganhayyatha, </i>literalmente, “você pode possuir esse objeto.” Isto se
conecta com o verso 18 acerca da agitação por posses externas. [<a href="#R14">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N15></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[15] <i>Attavadupadanam upadiyetha, </i>literalmente, “você pode se
apegar a esse apego a uma doutrina de um eu”. Este trecho se conecta com o verso
20 acerca da agitação que surge de uma idéia acerca de um eu. [<a href="#R15">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N16></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[16] O
apoio em idéias, <i>(ditthinissaya)</i>, de acordo com MA são as 62 idéias
mencionadas no <a href="DN1.php">Brahmajala Sutta</a> (DN 1) que emergem da
idéia da identidade ou “doutrina de um eu”. Também pode incluir a idéia
perniciosa adotada por Arittha no início do sutta. [<a href="#R16">Retorna</a>]<o:p></o:p></span></p>

<p class=Normal><a name=N17></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[17] A
noção “que pertence ao eu” ou “propriedade de um eu,” (<i>attaniya)</i>, é atribuída
a qualquer um dos cinco agregados que não seja identificado como eu, bem
como a todas as posses externas do indivíduo. Esta passagem mostra a
dependência mútua e dessa forma a falta de sustentação das noções gêmeas “eu” e
“meu”. [<a href="#R17">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N18></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[18] De
acordo com os comentários, desencantamento, (<i>nibbida</i> - também
interpretado como “náusea” ou “nojo”), significa o estágio máximo de insight,
desapego, (<i>viraga)</i>, é realizar o caminho supramundano e libertação, (<i>vimutti)</i>, o fruto.[<a href="#R18">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N19></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[19] “Assim ido ou assim vindo” é em Pali, <i>tathagata, </i>o
epiteto usual do Buda, mas neste caso aplicado de uma maneira mais ampla ao
arahant. MA interpreta este trecho de duas formas alternativas: (1) O arahant
mesmo estando vivo não pode ser detectado no aqui e agora como um ser ou
indivíduo (no sentido da presença de um eu) porque em última instância não
existe um ser (um ‘eu’). (2) O arahant não pode ser detectado no aqui e agora
porque é impossível que os devas, etc. encontrem o suporte para a sua
consciência de insight, a consciência do caminho supramundano ou a consciência
do fruto, (<i>vipassanacitta, maggacitta, phalacitta) </i>; isto é, sendo Nibbana
o objeto, a sua mente não pode ser conhecida pelo ser mundano. [<a href="#R19">Retorna]</a><o:p></o:p></span></p>



<p class=Normal><a name=N20></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[20] Isto se refere ao verso 20 onde aquele que crê no eterno confunde o
ensinamento do Buda acerca de Nibbana, a cessação do ser, como envolvendo a aniquilação
de um ser considerado como sendo o ‘eu’. [<a href="#R20">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N21></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[21] A importância desta afirmação é mais profunda do que parece. No
contexto das falsas acusações do verso 37, o Buda afirma que ele ensina que um
ser vivo não é um eu mas um mero conglomerado de fatores, eventos materiais e
mentais, conectados num processo que é inerentemente <i>dukkha </i>e que
Nibbana, a cessação do sofrimento, não é a aniquilação de um ser mas o término
desse processo insatisfatório. Esta afirmação deve ser lida em conjunto com o <a
href="SNXII.15.php">SN XII.15</a>, onde o Buda diz que uma pessoa com o
entendimento correto, que deixou de lado todas as doutrinas de um eu, vê que
tudo aquilo que surge é somente <i>dukkha </i>surgindo e tudo aquilo que cessa
é somente <i>dukkha </i>cessando. [<a href="#R21">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N22></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[22] “Aquilo que já foi completamente compreendido,” (<i>pubbe
pariññatam)</i>, são os cinco agregados. Já que são somente eles que recebem as
demonstrações de honra e ofensa, não um ‘eu’, não existe razão para a
sublimidade ou depressão. [<a href="#R22">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N23></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[23] MA indica que é o apego aos cinco agregados que deve ser
abandonado; os agregados em si não podem ser despedaçados ou arrancados. [<a
href="#R23">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N24></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[24] MA: “<i>Chinna-pilotika: pilotika </i>é um trapo rasgado e gasto,
remendado e com nós aqui e ali; não existe nada (no Dhamma) como isso –
rasgado, gasto, remendado e com nós representados pela hipocrisia e outras
enganações. [<a href="#R24">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N25></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[25] Isto é: como os arahants alcançaram a libertação de todo o ciclo de
existência, é impossível apontar algum mundo, dentro do ciclo, em que eles
possam renascer. [<a href="#R25">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N26></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[26] Essas são as duas classes de indivíduos que se encontram no caminho
de ‘entrar na correnteza’. “Discípulos do Dhamma,” (<i>dhammanusarin)</i>, são
discípulos, para quem a faculdade da sabedoria, (<i>paññindryia)</i>, é
predominante, que desenvolvem o nobre caminho liderados pela sabedoria; quando
atingem o fruto, eles são chamados de “aqueles que realizaram a visão,” (<i>ditthipatta).
</i>“Discípulos pela Fé,” (<i>saddhanusarin)</i>, são discípulos, para quem a
faculdade da fé, (<i>saddhindriya)</i>, é predominante, que desenvolvem o nobre
caminho liderados pela fé; quando alcançam o fruto, eles são chamados de
“liberados pela fé,” (<i>saddhavimutta). </i>[<a href="#R26">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N27></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[27] MA diz que isto se refere a pessoas dedicadas à prática da
meditação de insight que ainda não obtiveram nenhuma realização supramundana.
Note que eles estão destinados apenas ao paraíso, não à iluminação. Mas, se a
sua prática amadurecer, eles podem alcançar o caminho de ‘entrar na correnteza’
e dessa forma obter a segurança de alcançar a iluminação. A expressão <i>saddhamattam
pemamattam </i>pode ser interpretada como “simplesmente fé, simplesmente amor”
ou “só fé, só amor”, mas isto não explica a garantia de renascimento no
paraíso. Portanto, parece obrigatório termos que usar o sufixo <i>matta</i> significando uma certa quantidade necessária de fé e amor e não simplesmente
a posse dessas qualidades. [<a href="#R27">Retorna</a>]<o:p></o:p></span></p>


