<p class=Normal style='margin-left:.75in'>



<p class=Normal>Em certa ocasião o
Abençoado estava em Savatthi, no Bosque de Jeta, no Parque de Anathapindika.<span
style="mso-spacerun: yes">  </span>Então o chefe de família Anathapindika
deixou<span style="mso-spacerun: yes">  </span>Savatthi cedo pela manhã para
ver o Abençoado, porém o pensamento lhe ocorreu, “Não é o momento apropriado
para ver o Abençoado, ele ainda está em retiro. E não é o momento
apropriado para ver os bhikkhus dignos de respeito, eles ainda estão em
retiro. E se eu fosse até o parque dos errantes de outras seitas?&quot; E
ele foi até o parque onde os errantes de outras seitas estavam. <o:p></o:p></span></p>


<p class=Normal>
Agora, naquela ocasião os errantes de outras seitas estavam sentados
numa grande assembléia fazendo uma grande baderna, conversando em voz alta e
aos berros sobre muitos assuntos inúteis. Eles viram o chefe de família Anathapindika vindo à distância. Ao vê-lo, eles silenciaram a assembléia dizendo o
seguinte: “Senhores, fiquem quietos; senhores, não façam ruído. Ali vem o chefe
de família Anathapindika, um discípulo do contemplativo Gotama, um dos
discípulos leigos vestidos de branco do contemplativo Gotama que está 
em Savatthi. Esses veneráveis gostam do silêncio; eles são disciplinados no
silêncio; eles recomendam o silêncio. Talvez, se ele encontrar a nossa
assembléia em silêncio, ele pensará em juntar-se a nós.” Então os errantes
ficaram em silêncio.<o:p></o:p></span></p>


<p class=Normal>
Então o chefe de família Anathapindika foi até onde os errantes de
outras seitas estavam e eles se cumprimentaram. Quando a conversa amigável e
cortês havia terminado, ele sentou a um lado e os errantes lhe disseram: <o:p></o:p></span></p>


<p class=Normal>
“Diga-nos, chefe de família, qual entendimento tem o contemplativo
Gotama.&quot; <o:p></o:p></span></p>


<p class=Normal>
&quot;Veneráveis senhores, eu não conheço por completo o entendimento
do Abençoado.&quot; <o:p></o:p></span></p>


<p class=Normal>
&quot;Bem, bem. Então você não conhece por completo o entendimento do
contemplativo Gotama. Então diga-nos qual entendimento<span
style="mso-spacerun: yes">  </span>têm os bhikkhus.&quot; <o:p></o:p></span></p>


<p class=Normal>
&quot;Eu nem mesmo sei por completo o entendimento dos bhikkhus.&quot; <o:p></o:p></span></p>


<p class=Normal>
&quot;Então você não sabe por completo o entendimento do contemplativo Gotama
nem mesmo dos bhikkhus. Então diga-nos qual entendimento você tem.&quot; <o:p></o:p></span></p>


<p class=Normal>
&quot;Não seria difícil para eu expor para vocês qual entendimento
tenho. Porém, por favor, que os veneráveis exponham cada um de acordo com a sua
posição, e então não será difícil para eu expor para vocês qual entendimento
tenho.&quot; <o:p></o:p></span></p>


<p class=Normal>
Quando isto foi dito, um dos errantes disse para o chefe
de família Anathapindika &quot;<i>O mundo é eterno.</i>
Somente isso é verdadeiro; todo o restante é falso. Esse é o tipo de
entendimento que eu tenho.&quot; <o:p></o:p></span></p>


<p class=Normal>
Outro errante disse para Anathapindika, &quot;<i>O mundo não é eterno.</i> Somente isso é verdadeiro; todo o restante é
falso. Esse é o tipo de entendimento que eu tenho.&quot; <o:p></o:p></span></p>


<p class=Normal>
Um outro errante disse,<span style="mso-spacerun: yes">  </span><i>&quot;O mundo é finito...&quot;...&quot;O
mundo é infinito....&quot;...&quot;A alma e o corpo são a mesma coisa
...&quot;...&quot;A alma é uma coisa e o corpo outra...&quot;...&quot;Após a
morte um Tathagata existe...&quot;...&quot;Após a morte um Tathagata não
existe...&quot;...&quot;Após a morte um Tathagata <span style='mso-bidi-font-style:
italic'>tanto existe como não existe</span>...&quot;...&quot;Após a morte um
Tathagata nem existe nem não existe.</i> Somente isso é verdadeiro; todo o
restante é falso.<span style="mso-spacerun: yes">  </span>Esse é o tipo de
entendimento que eu tenho.&quot; <o:p></o:p></span></p>


<p class=Normal>
Quando isto foi dito, o chefe
de família Anathapindika disse aos
errantes, &quot;Quanto ao venerável que diz, '<i style='mso-bidi-font-style:
normal'>O mundo é eterno.</i> Somente isso é verdadeiro; todo o restante é
falso.<span style="mso-spacerun: yes">  </span>Esse é o tipo de entendimento
que eu tenho,' seu entendimento surge da sua atenção inadequada ou na
dependência das palavras de outrem. Agora, esse entendimento é fabricado, é
condicionado, surge através da volição, tem origem dependente. Tudo aquilo que
é fabricado, é condicionado, surge através da volição, tem origem dependente, é
impermanente. Tudo aquilo que é impermanente é sofrimento. Esse venerável
portanto se une a esse sofrimento, submete-se a esse sofrimento.&quot; (Da mesma
forma para as outras posições.) <o:p></o:p></span></p>


<p class=Normal>
Quando isto foi dito, os errantes disseram para o chefe
de família Anathapindika  &quot;Cada um de nós expôs de acordo com a sua posição. Agora
diga-nos qual entendimento você tem.&quot; <o:p></o:p></span></p>


<p class=Normal>
&quot;Tudo aquilo que é fabricado, é condicionado, surge através da
volição, tem origem dependente, isso é impermanente. Tudo aquilo que é
impermanente é sofrimento. Tudo aquilo que é sofrimento, isso não é meu, isso não sou eu, isso não é o meu eu. Esse é o tipo de entendimento que eu tenho.&quot; <o:p></o:p></span></p>

<p class=Normal>
&quot;Portanto, chefe de família, tudo aquilo que é fabricado, é
condicionado, surge através da volição, tem origem dependente, isso é
impermanente. Tudo aquilo que é impermanente é sofrimento. Você portanto se une
a esse mesmo sofrimento, submete-se a esse sofrimento.&quot; <o:p></o:p></span></p>


<p class=Normal>
&quot;Veneráveis senhores, tudo aquilo que é fabricado, é condicionado,
surge através da volição, tem origem dependente, isso é impermanente. Tudo
aquilo que é impermanente é sofrimento. Tudo aquilo que é sofrimento, isso não é meu, isso não sou eu, isso não é o meu eu. Tendo visto isso bem, com correta
sabedoria, como na verdade é, eu também compreendo, tal como na verdade é,
a mais elevada libertação disso.&quot; <o:p></o:p></span></p>


<p class=Normal>
Quando isso foi dito, os errantes ficaram sentados em silêncio,
consternados, com os ombros caídos e a cabeça baixa, deprimidos e sem resposta.
Anathapindika  percebendo que os errantes estavam sentados
em silêncio, consternados ... sem resposta, foi até o Abençoado e depois de cumprimentá-lo sentou
a um lado e 
relatou ao Abençoado toda a conversa com os errantes. <o:p></o:p></span></p>


<p class=Normal>
[O Abençoado disse:] Muito bem, chefe de família. Muito bem. Assim é
como você deve sempre refutar com o Dhamma esses homens tolos.” E então ele
instruiu, estimulou, motivou e encorajou o chefe
de família Anathapindika com
um discurso do Dhamma. 

<p class=Normal>Quando Anathapindika havia sido
instruído, estimulado, motivado e encorajado pelo Abençoado com um discurso do
Dhamma, ele levantou do seu assento e depois de homenagear o Abençoado,
mantendo-o à sua direita, partiu. </p>

<p class=Normal>Não muito tempo depois o Abençoado dirigiu-se
aos bhikkhus: “Bhikkhus, mesmo um bhikkhu que há<span style="mso-spacerun:
yes">  </span>muito tenha compreendido o Dhamma nesta Doutrina e Disciplina
faria bem em sempre refutar os errantes de outras seitas com o Dhamma
exatamente da forma como fez o chefe
de família Anathapindika.&quot; <o:p></o:p></span></p>

