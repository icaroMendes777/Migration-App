<p class=Tit3 align=center style='text-align:center'><b> Anguttara Nikaya X.93</b></Tit3>
<p class=Tit1 align=center style='text-align:center'>Ditthi Sutta </Tit1>
<p class=Tit1 align=center style='text-align:center'>Entendimento </Tit1>

<p class=Normal align=center style='text-align:center'><span lang=PT-BR style='font-size:
