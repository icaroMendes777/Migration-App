<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ouvi que
em certa ocasião o Venerável Ananda estava em Kosambi, no Parque de Ghosita.
Então uma certa bhikkhuni disse a um certo homem, “Vá, meu bom homem, para o
meu senhor Ananda e preste uma homenagem em meu nome com a sua cabeça aos pés dele e 
lhe diga, ‘A bhikkhuni com nome tal, venerável senhor, está enferma, com dores,
gravemente doente. Ela homenageia o seu senhor Ananda com a cabeça aos seus pés e diz,
“Seria bom se o meu senhor Ananda viesse ao alojamento das bhikkhunis, para
visitar essa bhikkhuni por compaixão.”’” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Respondendo, “Sim, minha
senhora” o homem então se aproximou do Ven. Ananda e, chegando, depois de
cumprimentá-lo, sentou a um lado e disse para o Ven. Ananda,<span
style="mso-spacerun: yes">  </span>‘A bhikkhuni com nome tal, venerável senhor,
está enferma, com dores, gravemente doente. Ela homenageia o seu senhor Ananda com a cabeça aos seus pés e diz, “Seria bom se o meu senhor Ananda viesse ao alojamento das
bhikkhunis, para visitar essa bhikkhuni por compaixão.”’ <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O Ven. Ananda concordou em
silêncio. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim, ao amanhecer, o Ven.
Ananda se vestiu e </span>tomando a tigela e o manto externo<span lang=PT-BR
style='mso-ansi-language:PT-BR'> ele se dirigiu ao alojamento das bhikkhunis. A
bhikkhuni viu o Ven. Ananda vindo à distância. Ao vê-lo ela se deitou na cama,
cobrindo a cabeça. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o Ven. Ananda foi até
a bhikkhuni e sentou em um assento que havia sido preparado e disse para a
bhikkhuni: “Este corpo, irmã, surge através da comida. E no entanto é contando
com a comida que a comida deve ser abandonada. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Este corpo surge através do
desejo. E no entanto é contando com o desejo que o desejo deve ser abandonado. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Este corpo surge através
da presunção. E no entanto é contando com a presunção que a presunção deve ser
abandonada. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Este corpo surge através
da relação sexual. E no entanto é contando com a relação sexual que a relação
sexual deve ser abandonada. O Buda chama a relação sexual de romper a ponte.[<a
href="#N1">1</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Este corpo,
irmã, surge através da comida. E no entanto é contando com a comida que a
comida deve ser abandonada. Assim foi dito. E em referência a que foi dito
isso?<span style="mso-spacerun: yes">  </span>É o caso, irmã, em que um
bhikkhu,<span style="mso-spacerun: yes">  </span>refletindo de maneira sábia: o
alimento não deve ser tomado como forma de diversão ou para embriaguez,
tampouco com o objetivo de embelezamento e para ser mais atraente porém,
somente com o propósito de manter a resistência e continuidade desse corpo,
como forma de dar um fim ao desconforto e para auxiliar a vida santa.
Considerando: ‘Dessa forma darei um fim às antigas sensações (de fome) sem
despertar novas sensações (de comida em excesso) e serei saudável e sem culpa e
viverei em comodidade.’ Então, mais tarde, ele abandona a comida, tendo
dependido da comida. 'Este corpo, irmã, surge através da comida. E no entanto é
contando com a comida que a comida deve ser abandonada.’ Assim foi dito. E em
referência a isso é que foi dito.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’Este corpo surge através
do desejo. E no entanto é contando com o desejo que o desejo deve ser
abandonado. Assim foi dito. E em referência a que foi dito isso? É o caso, irmã
em que um bhikkhu, ouve, ‘O bhikkhu com o nome tal, dizem, eliminou as
impurezas mentais <i style='mso-bidi-font-style:normal'>(asava)</i>, e </span>permanece
em um estado livre de impurezas com a libertação da mente e a libertação através da  sabedoria,
tendo conhecido e manifestado isso para si mesmo no aqui e agora<span
lang=PT-BR style='mso-ansi-language:PT-BR'>’. O pensamento lhe ocorre, ‘Espero
que eu, também, irei – eliminar as impurezas - </span>permanecendo em um estado
livre de impurezas com a libertação da mente e a libertação através da sabedoria,
tendo conhecido e manifestado isso para mim mesmo no aqui e agora<span
lang=PT-BR style='mso-ansi-language:PT-BR'>.’ Então, mais tarde, ele
abandona<span style="mso-spacerun: yes">  </span>o desejo, tendo dependido do
desejo. ‘Este corpo surge através do desejo. E no entanto é contando com o
desejo que o desejo deve ser abandonado.’ Assim foi dito. E em referência a
isso é que foi dito.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><span style="mso-spacerun:
yes"> </span>“’Este corpo surge através da presunção. E no entanto é contando
com a presunção que a presunção deve ser abandonada.’ Assim foi dito. E em
referência a o que foi dito isso? É o caso, irmã em que um bhikkhu ouve ‘O
bhikkhu com o nome tal, dizem, eliminou as impurezas mentais <i
style='mso-bidi-font-style:normal'>(asava)</i>, e </span>permanece em um estado
livre de impurezas com a libertação da mente e a libertação através da sabedoria,
tendo conhecido e manifestado isso para si mesmo no aqui e agora<span
lang=PT-BR style='mso-ansi-language:PT-BR'>’. O pensamento lhe ocorre, ‘O
bhikkhu com o nome tal, dizem, eliminou as impurezas mentais <i
style='mso-bidi-font-style:normal'>(asava)</i>, e </span>permanece em um estado
livre de impurezas com a libertação da mente e a libertação através da sabedoria,
tendo conhecido e manifestado isso para si mesmo no aqui e agora<span
lang=PT-BR style='mso-ansi-language:PT-BR'>. Então por que não eu?’ Então, mais
tarde, ele abandona<span style="mso-spacerun: yes">  </span>a presunção, tendo
dependido da presunção. ‘Este corpo surge através da presunção. E no entanto é
contando com a presunção que a presunção deve ser abandonada.’ Assim foi dito.
E em referência a isso é que foi dito<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Este corpo surge através
da relação sexual. A relação sexual deve ser abandonada. O Buda chama a relação
sexual de romper a ponte.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então a bhikkhuni –
levantando-se da sua cama, arrumando o seu manto superior sobre um ombro e
curvando-se com a sua cabeça aos pés do Ven. Ananda – disse, “Uma transgressão
foi cometida por mim, venerável senhor, ao ter sido tão tola, tão atrapalhada e
tão inábil em agir dessa forma. Possa meu senhor Ananda por favor aceitar esta
confissão da minha transgressão como tal, de forma eu possa me conter no
futuro.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim, irmã, você cometeu
uma transgressão, ao ter sido tão tola, tão atrapalhada e tão inábil em agir
dessa forma. Porém como você vê a sua transgressão dessa forma e faz a correção
de acordo com o Dhamma, nós aceitamos a sua confissão. Pois é motivo de
crescimento no Dhamma e Disciplina dos nobres quando, vendo uma transgressão
como tal, a pessoa faz correções de acordo com o Dhamma e pratica a contenção
no futuro.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o que o<span
style="mso-spacerun: yes">  </span>Ven. Ananda disse. A bhikkhuni</span><span
style='mso-bookmark:PVW'> ficou satisfeita e contente com as palavras do</span><span
style='mso-bookmark:PVW'><span lang=PT-BR style='mso-ansi-language:PT-BR'> Ven</span></span><span
lang=PT-BR style='mso-ansi-language:PT-BR'>. Ananda. <o:p></o:p></span></p>


