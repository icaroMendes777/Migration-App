<p class=Normal>
Em certa ocasião o Abençoado estava  em Rajagaha, no Bambual, no Santuário dos Esquilos. Então o brâmane Vassakara, o ministro de Magadha foi até o Abençoado e ambos se cumprimentaram. Quando a conversa cortês e amigável havia terminado ele sentou a um lado e disse para o Abençoado: </p>

<p class=Normal>
 “Minha idéia, minha opinião, é que quando alguém fala sobre aquilo que ele viu, [dizendo] ‘Assim eu vi,’ não há nenhum erro nisso. Quando alguém fala sobre aquilo que ele ouviu, [dizendo] ‘Assim eu ouvi,’ não há nenhum erro nisso. Quando alguém fala sobre aquilo que ele sentiu, [dizendo] ‘Assim eu senti,’ não há nenhum erro nisso. Quando alguém fala sobre aquilo que ele conscientizou, [dizendo] ‘Assim eu conscientizei,’ não há nenhum erro nisso." </p>

<p class=Normal>
 [O Abençoado respondeu:] “Eu não digo, brâmane, que tudo aquilo que tenha sido visto deva ser falado. Nem digo que tudo aquilo que tenha sido visto não deva ser falado. Eu não digo que tudo aquilo que tenha sido ouvido … tudo aquilo que tenha sido sentido ... tudo aquilo que tenha sido conscientizado deva ser falado. Nem digo que tudo aquilo que tenha sido conscientizado não deva ser falado. </p>

<p class=Normal>
 “Quando, em alguém que fala sobre aquilo que foi visto, as qualidades mentais inábeis aumentam e as qualidades mentais hábeis diminuem, então esse tipo de coisa não deve ser falada. Mas quando, alguém que fala sobre aquilo que foi visto, as qualidades mentais inábeis diminuem e as qualidades mentais hábeis aumentam, então esse tipo de coisa deve ser falada. </p>

<p class=Normal>
 “Quando, em alguém que fala sobre aquilo que foi ouvido ... aquilo que foi sentido … aquilo que foi conscientizado, as qualidades mentais inábeis aumentam e as qualidades mentais hábeis diminuem, então esse tipo de coisa não deve ser falada. Mas quando, alguém que fala sobre aquilo que foi conscientizado, as qualidades mentais inábeis diminuem e as qualidades mentais hábeis aumentam, então esse tipo de coisa deve ser falada.” </p>

<p class=Normal>
Então o brâmane Vassakara satisfeito e contente com as palavras do Abençoado, levantou do seu assento e partiu. </p>

