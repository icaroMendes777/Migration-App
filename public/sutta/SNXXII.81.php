<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em certa
ocasião o Abençoado estava em Kosambi, no Parque de Ghosita. Então, ao
amanhecer, o Abençoado se vestiu e tomando a tigela e o manto externo, foi para
Kosambi para esmolar alimentos. Depois de haver esmolado em Kosambi e de haver
retornado, após a refeição ele arrumou a sua habitação e – sem chamar o seu
acompanhante ou avisar a comunidade de bhikkhus – saiu caminhando só e sem
companhia.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, não
muito tempo depois que o Abençoado havia saído, um certo bhikkhu foi até o ven.
Ananda e ao chegar disse, “Agora mesmo, Ananda, o Abençoado arrumou a sua
habitação e – sem chamar o seu acompanhante ou avisar a comunidade de bhikkhus
– saiu caminhando só e sem companhia.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sempre que
o Abençoado arruma a sua habitação e – sem chamar o seu acompanhante ou avisar
a comunidade de bhikkhus – sai caminhando só e sem companhia, ele quer ficar
só. Ninguém deve seguí-lo nessas ocasiões.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então,
depois de caminhar durante algum tempo, o Abençoado chegou em Palileyyaka. Lá
ele sentou ao pé de uma auspiciosa árvore Sal. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, um
grande número de bhikkhus foram até o ven. Ananda e ao chegar o cumprimentaram.
Depois da troca de saudações amigáveis e corteses eles sentaram a um lado e
disseram: “Já faz muito tempo que não ouvimos um discurso do Dhamma na presença
do Abençoado. Nós gostaríamos de ouvir um discurso do Dhamma na presença do
Abençoado.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, o ven. Ananda foi com aqueles bhikkhus até onde estava o Abençoado
em Palileyyaka, ao pé da auspiciosa árvore Sal<span style="mso-spacerun: yes"> 
</span>e ao chegar, depois de cumprimentá-lo, sentaram a um lado e o Abençoado
os instruiu, motivou, estimulou e encorajou com um discurso do Dhamma.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Agora,
naquela ocasião um pensamento surgiu na mente de um dos bhikkhus: “Como alguém
deve compreender, como alguém deve ver, para que ocorra a imediata destruição
das impurezas?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O
Abençoado, percebendo com a sua mente o pensamento na mente daquele bhikkhu,
disse aos bhikkhus: <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
este Dhamma foi ensinado por mim com base na investigação profunda. Os quatro
fundamentos da atenção plena foram ensinados com base na investigação profunda.
Os quatro esforços corretos ... As quatro bases do poder ... As cinco faculdades
... Os cinco poderes ... Os sete fatores da iluminação ... O nobre caminho
óctuplo foi ensinado por mim com base na investigação profunda. Bhikkhus, com
respeito ao Dhamma ensinado por mim com base na investigação profunda, um
pensamento surgiu na mente de um dos bhikkhus: ‘Como alguém deve compreender,
como alguém deve ver, para que ocorra a imediata destruição das impurezas?’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como,
bhikkhus, alguém deve compreender, como alguém deve ver, para que ocorra a
imediata destruição das impurezas? É o caso em que uma pessoa comum sem instrução,
que não respeita os nobres, que não é proficiente nem treinada no Dhamma
deles, que não respeita os homens verdadeiros, que não é proficiente nem
treinada no Dhamma deles, considera que a forma é o eu. Essa consideração é
uma formação. Agora, qual é a causa, qual é a origem e do que essa formação
nasce e é produzida? Para uma pessoa comum sem instrução, tocada pela sensação
que nasce do contato e da ignorância, o desejo surge: assim nasce essa
formação. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
bhikkhus, essa formação é impermanente, condicionada, com origem dependente.
Esse desejo ... Essa sensação ... Esse contato ... Essa ignorância é
impermanente, condicionada e com origem dependente. Quando alguém compreende e
vê dessa forma, bhikkhus, ocorre a imediata destruição das impurezas.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ela pode
não considerar que a forma é o eu, mas ela considera o eu possuído de forma, ...
a forma como estando no eu, ... o eu como estando na forma. Ou pode não
considerar que a sensação seja o eu, mas ela considera o eu possuído de sensação, ... a sensação
como estando no eu, ... o eu como estando na sensação. Ou pode não considerar
que a percepção seja o eu, mas ela considera o eu possuído de percepção, ... a percepção
como estando no eu, ... o eu como estando na percepção. Ou pode não
considerar que as formações volitivas sejam o eu, mas ela considera o eu possuído de formações volitivas, ... as formações volitivas como estando no eu, ... o
eu como estando nas formações volitivas. Ou pode não considerar que a consciência seja o eu, mas ela considera o eu possuído de consciência, ... a
consciência como estando no eu, ... o eu como estando na consciência. Essa
consideração é uma formação. Agora, qual é a causa, qual é a origem e do que essa formação
nasce e é produzida? Para uma pessoa comum sem instrução, tocada pela sensação
que nasce do contato e da ignorância, o desejo surge: assim nasce essa
formação.</p> 

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
bhikkhus, essa formação é impermanente, condicionada, com origem dependente.
Esse desejo ... Essa sensação ... Esse contato ... Essa ignorância é
impermanente, condicionada e com origem dependente. Quando alguém compreende e
vê dessa forma, bhikkhus, ocorre a imediata destruição das impurezas.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele pode
não considerar que a forma é o eu ... o eu como estando na consciência, mas ele
poderá ter uma idéia como esta: ‘Aquilo que é o eu é o mundo; depois de
falecer, assim serei: permanente, estável, eterno, não sujeito a mudanças.’
Essa idéia da eternidade é uma formação. Agora, qual é a causa, qual é a origem e do que essa formação
nasce e é produzida? Para uma pessoa comum sem instrução, tocada pela sensação
que nasce do contato e da ignorância, o desejo surge: assim nasce essa
formação.</p> 

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
bhikkhus, essa formação é impermanente, condicionada, com origem dependente.
Esse desejo ... Essa sensação ... Esse contato ... Essa ignorância é
impermanente, condicionada e com origem dependente. Quando alguém compreende e
vê dessa forma, bhikkhus, ocorre a imediata destruição das impurezas.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele pode
não considerar que a forma é o eu ... ou ter essa idéia da eternidade, mas ele
poderá ter uma idéia como esta: ‘Eu poderei não ser; isso poderá não ser para
mim; eu não serei; isso não será para mim.’ Essa idéia de aniquilação é uma
formação. Agora, qual é a causa, qual é a origem e do que essa formação
nasce e é produzida? Para uma pessoa comum sem instrução, tocada pela sensação
que nasce do contato e da ignorância, o desejo surge: assim nasce essa
formação.</p> 

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
bhikkhus, essa formação é impermanente, condicionada, com origem dependente.
Esse desejo ... Essa sensação ... Esse contato ... Essa ignorância é
impermanente, condicionada e com origem dependente. Quando alguém compreende e
vê dessa forma, bhikkhus, ocorre a imediata destruição das impurezas.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele pode
não considerar que a forma é o eu ... ou ter essa idéia de aniquilação, mas ele
está perplexo, com dúvidas e incertezas em relação ao verdadeiro Dhamma. Essa
perplexidade, dúvida e incerteza em relação ao verdadeiro Dhamma é uma
formação. Agora, qual é a causa, qual é a origem e do que essa formação nasce e
é produzida?<span style="mso-spacerun: yes">  </span>Para uma pessoa sem
instrução, tocada pela sensação que nasce do contato e da ignorância, o desejo
surge: assim nasce essa formação.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
essa formação, bhikkhus, é impermanente, condicionada e com origem dependente.
Esse desejo ... Essa sensação ... Esse contato ... Essa ignorância é
impermanente, condicionada e com origem dependente. Quando alguém compreende e
vê dessa forma, bhikkhus, ocorre a imediata destruição das impurezas.<o:p></o:p></span></p>


