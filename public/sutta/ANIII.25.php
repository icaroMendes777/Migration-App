<p class=Normal> “Bhikkhus, existem três tipos de indivíduos que podem ser encontrados no mundo. Quais três? Um indivíduo tem a mente como uma ferida aberta; um indivíduo tem a mente como um relâmpago; um indivíduo tem a mente como um diamante. </p>

<p class=Normal> “E qual é a natureza, bhikkhus, do indivíduo com a mente como uma ferida aberta? Ele é irascível e irritável. Mesmo se ele for criticado apenas levemente ele fica zangado e preocupado; ele é teimoso e demonstra raiva, ódio e ressentimento. Tal como, por exemplo, uma ferida supurada, se for tocada com uma vareta ou um caco, irá supurar ainda mais matéria, assim também esse indivíduo é irascível ... demonstra raiva, ódio e ressentimento. Este é um indivíduo do qual é dito ter a mente como uma ferida aberta. </p>

<p class=Normal> “E qual é a natureza, bhikkhus, do indivíduo com a mente como um relâmpago? Ele compreende como na verdade é, ‘Isto é sofrimento’; Ele compreende como na verdade é, ‘Esta é a origem do sofrimento’; Ele compreende como na verdade é, ‘Esta é a cessação do sofrimento’;  Ele compreende como na verdade é,  ‘Este é o caminho que conduz à cessação do sofrimento.’ Tal como um homem com boa visão é capaz de ver formas na escuridão da noite por meio do brilho de um relâmpago, assim também esse indivíduo compreende essas Quatro Nobres Verdades como elas na verdade são. Este é um indivíduo do qual é dito ter a mente como um relâmpago. </p>

<p class=Normal> “E qual é a natureza, bhikkhus, do indivíduo com a mente como um diamante? Ele é aquele que compreendendo por si mesmo com conhecimento direto, aqui e agora, entra e permanece na libertação da mente e na libertação através da sabedoria, que são imaculadas, com a destruição de todas as impurezas. Tal como não há nada que um diamante não possa cortar, seja uma pedra preciosa ou uma rocha, assim também esse indivíduo compreendendo por si mesmo com conhecimento direto, aqui e agora, entra e permanece na libertação da mente e na libertação através da sabedoria, que são imaculadas, com a destruição de todas as impurezas. Este é um indivíduo do qual é dito ter a mente como um diamante. </p>

