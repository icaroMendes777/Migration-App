<p class=Normal> "Bhikkhus há esses três tipos de fracassos. Quais três? Fracasso no comportamento virtuoso, fracasso na mente, fracasso no entendimento. </p>

<p class=Normal> (1) "E qual é o fracasso no comportamento virtuoso? Aqui, alguém mata seres vivos, toma aquilo que não é dado, tem conduta imprópria em relação aos prazeres dos sentidos, diz mentiras, fala com malícia, fala de forma grosseira, fala de forma frívola. Isso é chamado de fracasso no comportamento virtuoso. </p>

<p class=Normal> (2) "E o que é o fracasso na mente? Aqui, alguém é cobiçoso e tem má vontade. Isso é chamado fracasso na mente. </p>

<p class=Normal> (3) "E o que é fracasso no entendimento? Aqui, alguém tem o entendimento incorreto e distorcido: 'Não existe nada que é dado, nada que é oferecido, nada que é sacrificado; não existe fruto ou resultado de ações boas ou más; não existe este mundo, nem outro mundo; não existe mãe, nem pai; nenhum ser que renasça espontaneamente; não existem no mundo brâmanes nem contemplativos bons e virtuosos que, após terem conhecido e compreendido diretamente por eles mesmos, proclamem este mundo e o próximo.' [<a href="#N1">1</a>] <a name=R1></a>Isso é chamado fracasso no entendimento. </p>

<p class=Normal>"Devido ao fracasso no comportamento virtuoso, com a dissolução do corpo, após a morte, os seres renascem num plano de miséria, num destino ruim, nos mundos inferiores, no inferno. Devido ao fracasso na mente .... devido ao fracasso no entendimento, com a dissolução do corpo, após a morte, os seres renascem num plano de miséria, num destino ruim, nos mundos inferiores, no inferno. </p>

<p class=Normal>"Esses bhikkhus são os três tipos de fracasso. </p>

<p class=Normal>"Bhikkhus há esses três tipos de sucesso. Quais três? Sucesso no comportamento virtuoso, sucesso na mente, sucesso no entendimento. </p>

<p class=Normal> (1) "E qual é o sucesso no comportamento virtuoso? Aqui, alguém se abstém de matar seres vivos, de tomar aquilo que não é dado, da conduta imprópria em relação aos prazeres dos sentidos, de dizer mentiras, de falar com malícia, de falar de forma grosseira, de falar de forma frívola. Isso é chamado de sucesso no comportamento virtuoso. </p>

<p class=Normal> (2) "E o que é o sucesso na mente? Aqui, alguém não é cobiçoso e está livre da má vontade. Isso é chamado sucesso na mente. </p>

<p class=Normal> (3) "E o que é sucesso no entendimento? Aqui, alguém tem o entendimento correto e não distorcido: ‘Existe aquilo que é dado e o que é oferecido e o que é sacrificado; existe fruto e resultado de boas e más ações; existe este mundo e o outro mundo; existe a mãe e o pai; existem seres que renascem espontaneamente; existem no mundo brâmanes e contemplativos bons e virtuosos que, após terem conhecido e compreendido diretamente por eles mesmos, proclamam este mundo e o próximo.’ Isso é chamado sucesso no entendimento. </p>

<p class=Normal>"Devido ao sucesso no comportamento virtuoso, com a dissolução do corpo, após a morte, os seres renascem num destino feliz, no paraíso. Devido ao sucesso na mente .... devido ao sucesso no entendimento, com a dissolução do corpo, após a morte, os seres renascem num destino feliz, no paraíso. </p>

<p class=Normal>"Esses bhikkhus são os três tipos de sucesso.</p>

