<p class=Normal> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: "Há uma coisa, bhikkhus, que quando surge no mundo, surge em detrimento de muitas pessoas, causando dor em muitas pessoas, causando perda, dano e sofrimento para devas e humanos. Qual é essa coisa? É um cisma na sangha. Quando a sangha está dividida há brigas, recriminações, difamações, e expulsões. Nessa situação aqueles que não são devotos não são convertidos e aqueles que são devotos se afastam."</p>

<p class=MsoNormal style='margin-left:.75in'> Quem divide a sangha<br>
permanece num estado de sofrimento, no inferno,<br>
por toda duração de um éon.<br>
Deliciando-se com a dissensão, injusto,<br>
desprovido da segurança contra o cativeiro;<br>
ao dividir a sangha unida<br>
ele sofre no inferno por um éon.</p>


