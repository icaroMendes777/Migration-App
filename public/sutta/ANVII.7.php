<p class=Normal> Então Ugga, o ministro chefe do rei foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse: </p>

<p class=Normal>"É maravilhoso e admirável, venerável senhor, quão rico é Migara de Rohana, com tanta riqueza e propriedades." </p>

<p class=Normal>"Mas quão rico é Migara de Rohana? Quanta riqueza e propriedades ele possui?" </p>

<p class=Normal>"Ele possui cem mil unidades de ouro cunhadas, sem falar na prata." </p>

<p class=Normal>"Há esse tipo de riqueza, Ugga; isso eu não nego. Mas esse tipo de riqueza pode ser perdida devido ao fogo, água, reis, ladrões ou parentes detestáveis. No entanto, Ugga, há sete tipos de riqueza que não são perdidas devido ao fogo, água, reis, ladrões ou parentes detestáveis. Quais sete? A riqueza da convicção, a riqueza do comportamento virtuoso, a riqueza da vergonha de cometer transgressões, a riqueza do temor de cometer transgressões, a riqueza do aprendizado, a riqueza da generosidade, a riqueza da sabedoria. Esses sete tipos de riqueza não são perdidas devido ao fogo, água, reis, ladrões ou parentes detestáveis." </p>

<p class=Normal>A riqueza da convicção, a riqueza do comportamento virtuoso, <br>
a riqueza da vergonha e do temor de cometer transgressões, <br>
a riqueza do aprendizado, e da generosidade, <br>
com a sabedoria com  a sétima riqueza: </p>

<p class=Normal>quando alguém possui esses sete tipos de riqueza, <br>
quer seja homem ou mulher, <br>
é dito que não são pobres, <br>
que a sua vida não foi em vão. </p>

<p class=Normal>Assim, uma pessoa sábia, <br>
recordando-se dos ensinamentos do Buda, <br>
deve intencionar pela convicção e pelo comportamento virtuoso, <br>
realizada na visão do Dhamma. </p>


