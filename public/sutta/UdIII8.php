<p class=Normal> Assim, ouvi. Certa ocasião, o Abençoado estava em Savatthi, no Bosque de Jeta, no Parque de Anathapindika. Agora, naquela ocasião, depois de haverem esmolado comida e de haverem retornado, após a refeição, um certo número de bhikkhus estavam reunidos no pavilhão quando surgiu esta conversa: "Um bhikkhu esmolador de comida, amigos, enquanto caminha esmolando comida, de tempos em tempos ele ouve sons agradáveis com o ouvido, de tempos em tempos ele vê formas agradáveis com o olho, de tempos em tempos ele cheira aromas agradáveis com o nariz, de tempos em tempos ele saboreia sabores agradáveis com a língua, de tempos em tempos ele toca tangíveis agradáveis com o corpo. Um bhikkhu esmolador de comida, amigos, quando caminha esmolando é respeitado, reverenciado, honrado, venerado, e homenageado. Venham. amigos, sejamos esmoladores de comida e nós também, de tempos em tempos ouviremos sons agradáveis com o ouvido ... de tempos em tempos tocaremos tangíveis agradáveis com o corpo. E nós também seremos respeitados, reverenciados, honrados, venerados, e homenageados ao caminharmos esmolando comida." E essa conversa dos bhikkhus continuou sem chegar a um fim.</p>

<p class=Normal> Então o Abençoado saindo do seu isolamento no final da tarde, dirigiu-se para o pavilhão e, chegando, sentou-se em um assento que havia sido preparado. Ele então perguntou aos bhikkhus: “Bhikkhus, qual é o assunto que faz com que vocês estejam sentados juntos aqui agora? E qual é a discussão que foi interrompida?”</p>

<p class=Normal> "Depois de havermos esmolado comida e de havermos retornado, após a refeição, nos reunidos aqui no pavilhão quando surgiu esta conversa: 'Um bhikkhu esmolador de comida, amigos, enquanto caminha esmolando comida, de tempos em tempos ouve sons agradáveis com o ouvido ... de tempos em tempos toca tangíveis agradáveis com o corpo. E nós também seremos respeitados, reverenciados, honrados, venerados, e homenageados ao caminharmos esmolando comida.' Essa, venerável senhor foi a discussão que foi interrompida quando o Abençoado chegou."</p>

<p class=Normal> "Não é correto, bhikkhus, não é apropriado que vocês, membros de clãs, que pela fé deixaram a vida em família e seguiram a vida santa conversem sobre esse tipo de assunto. Quando vocês se reunirem, bhikkhus, vocês devem fazer uma de duas coisas: discutir o Dhamma ou observar o nobre silêncio.".</p>

<p class=Normal>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p>

<p class=Normal style='margin-left:.75in'> Um bhikkhu que esmola comida<br>
sustentando a si mesmo e mais ninguém, <br>
é estimado pelo devas,</br>
contanto que a sua intenção não seja o elogio e a fama.</p>

