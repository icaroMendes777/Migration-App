estudante brâmane Vasettha.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. Então o
estudante brâmane Vasettha disse para o estudante brâmane Bharadvaja: “O contemplativo Gotama está em Icchanangala, na floresta próxima a
Icchanangala, e acerca desse mestre Gotama existe essa boa reputação: ‘Esse
Abençoado </span>é um arahant, perfeitamente iluminado, consumado no
verdadeiro conhecimento e conduta, bem-aventurado, conhecedor dos mundos, um
líder insuperável de pessoas preparadas para serem treinadas, mestre de devas
e humanos, desperto, sublime<span lang=PT-BR style='mso-ansi-language:PT-BR'>.
