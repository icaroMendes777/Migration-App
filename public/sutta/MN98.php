Icchanangala, isto é, o brâmane Canki, o brâmane Tarukkha, o brâmane
Pokkharasati, o brâmane Janussoni, o brâmane Todeyya e outros brâmanes
prósperos e bem conhecidos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. Então,
enquanto os estudantes brâmanes, Vasettha e Bharadvaja, caminhavam e
perambulavam fazendo exercício, esta discussão teve início entre eles: “Como
uma pessoa é um brâmane?” O estudante brâmane Bharadvaja disse: “Quando uma
pessoa é bem nascida de ambos lados, com a descendência maternal e paternal
pura até a sétima geração passada, inatacável e impecável no que diz respeito
ao nascimento, então a pessoa é um brâmane.” O estudante brâmane Vasettha
disse: “Quando uma pessoa é virtuosa e cumpre as observâncias, então ela é um
brâmane.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. Mas o
estudante brâmane Vasettha não foi capaz de convencer o estudante brâmane
