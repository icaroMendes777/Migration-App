deitando o fardo e se emancipando:<br>
esse é o verdadeiro Brâmane.</p>


<p class=Normal style='margin-left:.75in'>
34. Com profundo entendimento, sábio,<br>
hábil na distinção entre o caminho e o descaminho,<br>
tendo alcançado o objetivo supremo:<br>
esse é o verdadeiro Brâmane.</p>


<p class=Normal style='margin-left:.75in'>
35. Reservado em relação aos chefes de família<br>
e da mesma forma em relação aos contemplativos,<br>
perambulando sem morada fixa e com poucas necessidades:<br>
esse é o verdadeiro Brâmane.</p>


<p class=Normal style='margin-left:.75in'>
36. Renunciando à violência contra todos os seres vivos,<br>
fracos ou fortes,<br>
que não mata ou faz com que outros matem:<br>
esse é o verdadeiro Brâmane.</p>


<p class=Normal style='margin-left:.75in'>
37. Amigável entre os hostis,<br>
pacífico entre os violentos,<br>
desapegado entre os apegados:<br>
esse é o verdadeiro Brâmane.</p>


<p class=Normal style='margin-left:.75in'>
38. Cobiça e raiva, presunção e desprezo,<br>
se despegaram igual à semente de mostarda <br>
da ponta duma agulha:<br>
esse é o verdadeiro Brâmane. </p>


<p class=Normal style='margin-left:.75in'>
39. Empregando linguagem gentil,<br>
instrutiva, verdadeira,<br>
sem ofender a ninguém:<br>
esse é o verdadeiro Brâmane.</p>


<p class=Normal style='margin-left:.75in'>
40. No mundo nunca toma nada<br>
que não tenha sido dado, quer seja longo ou curto,<br>
grande ou pequeno, bom ou ruim:<br>
esse é o verdadeiro Brâmane.</p>


<p class=Normal style='margin-left:.75in'>
41. Não deseja mais nada<br>
quer seja deste mundo ou do próximo,<br>
livre do desejo e emancipado:<br>
esse é o verdadeiro Brâmane.</p>


<p class=Normal style='margin-left:.75in'>
42. Não possui mais apegos,<br>
nem perplexidades, pois sabe,<br>
tendo mergulhado no Imortal:<br>
esse é o verdadeiro Brâmane.</p>


<p class=Normal style='margin-left:.75in'>
43. Neste mundo transcendendo todos os vínculos<br>
tanto com os méritos como com os deméritos,<br>
livre do sofrimento, imaculado e puro:<br>
esse é o verdadeiro Brâmane.</p>


<p class=Normal style='margin-left:.75in'>
44. Imaculado como a lua,<br>
puro, sereno e imperturbado,<br> 
tendo destruído o desejo por ser/existir:<br>
esse é o verdadeiro Brâmane.</p>


<p class=Normal style='margin-left:.75in'>
45. Superando esse <i>samsara</i> lodoso,<br>
perigoso, enganador,<br>
meditando com os <i>jhanas</i>, cruzando para a outra margem,<br>
imperturbável, sem perplexidade,<br>
realizando <i>nibbana</i> através do desapego:<br>
esse é o verdadeiro Brâmane.</p>


<p class=Normal style='margin-left:.75in'>
46. Abandonando os prazeres sensuais<br>
renunciando à vida em família, perambulando como um eremita,<br>
destruindo ambos o desejo pelos prazeres dos sentidos <br>
e pela continuada existência:<br>
esse é o verdadeiro Brâmane.</p>


<p class=Normal style='margin-left:.75in'>
47. Abandonando o desejo<br>
renunciando à vida em família, perambulando como um eremita,<br>
destruindo ambos o desejo <br>
e a continuada existência:<br>
esse é o verdadeiro Brâmane. </p>


<p class=Normal style='margin-left:.75in'>
48. Abandonando todos os vínculos humanos,<br>
e transcendendo todos os vínculos celestiais,<br>
desapegando-se de todos os vínculos:<br>
esse é o verdadeiro Brâmane.</p>


<p class=Normal style='margin-left:.75in'>
49. Abandonando o deleite e o descontentamento,<br>
arrefecido e sem apegos,<br>
o herói que transcendeu todo o mundo:<br>
esse é o verdadeiro Brâmane. </p>


<p class=Normal style='margin-left:.75in'>
50. Conhecendo de todos os modos <br>
a morte e o renascimento dos seres,<br>
desapegado, abençoado, iluminado:<br>
esse é o verdadeiro Brâmane. </p>


<p class=Normal style='margin-left:.75in'>
51. Cujo destino não é do conhecimento<br>
de devas e humanos,<br>
um arahant com as impurezas destruídas:<br>
esse é o verdadeiro Brâmane. </p>


<p class=Normal style='margin-left:.75in'>
52. Sem apego por absolutamente nada,<br>
do passado, presente e futuro,<br>
desimpedido e sem se agarrar a nada:<br>
esse é o verdadeiro Brâmane.</p>


<p class=Normal style='margin-left:.75in'>
53. O líder do rebanho, o perfeito herói,<br>
o grande sábio, o conquistador,<br>
imperturbável, imaculado, iluminado:<br>
esse é o verdadeiro Brâmane.</p>


<p class=Normal style='margin-left:.75in'>
54. Conhecendo as suas vidas passadas,<br>
vendo os mundos paradisíacos e os mundos inferiores,<br>
chegando ao fim dos nascimentos,<br>

esse é o verdadeiro Brâmane.</p>


<p class=Normal>12.</p>

<p class=Normal style='margin-left:.75in'><a name=T55></a>
55. “Pois o nome e o clã são
atribuídos<br>

como meras designações mundanas;<br>

com origem nas convenções,<br>

estes são atribuídos aqui e ali.</p>


<p class=Normal style='margin-left:.75in'>
56. Para aqueles que desconhecem
este fato,<br>

idéias incorretas há muito tempo
sustentam os seus corações;<br>

sem saber, eles nos declaram:<br>

‘Uma pessoa é brâmane pelo
nascimento.’</p>


<p class=Normal style='margin-left:.75in'>
57. Uma pessoa não é brâmane pelo
nascimento,<br>

nem pelo nascimento ela não é
brâmane.<br>

Através da ação ela é brâmane.<br>

Através da ação ela não é brâmane.</p>


<p class=Normal style='margin-left:.75in'>
58. Pois os homens são agricultores
através das suas ações,[<a href="#N5">5</a>]<a name=R5></a><br>

e através das suas ações eles
também são trabalhadores;<br>

e os homens são comerciantes
através das suas ações,<br>

e através das suas ações eles
também são criados.</p>


<p class=Normal style='margin-left:.75in'>
59. E os homens são ladrões através
das suas ações,<br>

e através das suas ações eles
também são soldados;<br>

e os homens são brâmanes através
das suas ações,<br>

e através das suas ações eles
também são governantes.</p>


<p class=Normal>13.</p>

<p class=Normal style='margin-left:.75in'>
60. “Portanto, é assim como aqueles
verdadeiramente sábios<br>

vêm as ações como elas na verdade
são,<br>

enxergando a origem dependente,<br>

com habilidade nas ações e nos seus
resultados. [<a href="#N6">6</a>]</p>


<p class=Normal style='margin-left:.75in'>
<a name=R6></a>61. As ações é que
fazem o mundo girar,<br>

as ações fazem esta geração girar.<br>

Os seres vivos estão atados pelas
ações,<br>

da mesma forma que a roda da
carruagem pelo eixo.</p>


<p class=Normal style='margin-left:.75in'>
62. Ascetismo, vida santa,<br>

autocontrole e treinamento interior
- <br>

assim uma pessoa se torna brâmane.<br>

Nisso se encontra o supremo estado
de brâmane. [<a href="#N7">7</a>]</p>


<p class=Normal style='margin-left:.75in'>
<a name=R7></a>63. Uma pessoa que
possui o conhecimento tríplice,<br>

pacífico, com o ser/existir
destruído:<br>

conheça-o assim, O! Vasettha,<br>

como Brahma e Sakka para aqueles
que compreendem.”</p>


<p class=Normal>14. Quando isso foi dito os estudantes brâmanes Vasettha e
Bharadvaja disseram para o Abençoado: “Magnífico, Mestre Gotama! Magnífico,
Mestre Gotama! Mestre Gotama esclareceu o Dhamma de várias formas, como se
tivesse colocado em pé o que estava de cabeça para baixo, revelasse o que
estava escondido, mostrasse o caminho para alguém que estivesse perdido ou
segurasse uma lâmpada no escuro para aqueles que possuem visão pudessem ver as
formas. Nós buscamos refúgio no Mestre Gotama, no Dhamma e na Sangha dos
bhikkhus. Que o Mestre Gotama nos aceite como discípulos leigos que nele
buscaram refúgio para o resto da sua vida.”</p>


