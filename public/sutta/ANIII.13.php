<p class=Normal>"Bhikkhus, há esses três tipos de pessoas que podem ser encontradas no mundo. Quais três? Aquela sem anseios, aquela cheia de anseios e aquela que superou os anseios.</p>

<p class=Normal> (1)"E como, bhikkhus, é a pessoa sem anseios? Aqui, alguém renasce numa família inferior – uma família de sudras, de cesteiros de bambu, de caçadores, de consertadores de carruagens ou de lixeiros – numa família em que há pouco para comer e beber e que sobrevive com dificuldades, onde comida e roupas são obtidas com dificuldades; e ele é feio, antiestético, deformado, doente crônico - peticego, ou com as mãos deformadas, ou coxo, ou paralítico. Ele não é um daqueles que ganham comida, bebida, roupas e veículos; grinaldas, perfumes e ungüentos; cama, moradia e lamparinas. Ele ouve: 'Os khattiyas ungiram a fulano de tal como um khattiya.' Então não lhe ocorre: 'Quando os khattiyas irão ungir a mim também?' Essa é chamada a pessoa sem anseios. </p>

<p class=Normal> (2) "E como é a pessoa cheia de anseios? Aqui, o filho mais velho de um rei khattiya ungido, que está destinado a ser ungido mas que ainda não foi ungido, alcançou a maturidade. Ele ouve: 'Os khattiyas ungiram a fulano de tal como um khattiya.' Então lhe ocorre: 'Quando os khattiyas irão ungir a mim também?' Essa é chamada a pessoa cheia de anseios. </p>

<p class=Normal> (3) "E como é a pessoa que superou os anseios? Aqui, um rei khattiya ungido ouve: 'Os khattiyas ungiram a fulano de tal como um khattiya.' Então não  lhe ocorre: 'Quando os khattiyas irão ungir a mim também?' Por qual razão? Porque o seu antigo anseio de ser ungido desapareceu ao ter sido ungido. Essa é chamada a pessoa que superou os anseios. </p>

<p class=Normal>"Esses são os três tipos de pessoas que podem ser encontradas no mundo. </p>

<p class=Normal>"Da mesma forma, bhikkhus, há três tipos de pessoas que podem ser encontradas existindo entre os bhikkhus. Quais três? Aquela sem anseios, aquela cheia de anseios e aquela que superou os anseios. </p>

<p class=Normal> (1)"E como, bhikkhus, é a pessoa sem anseios? Aqui, um bhikkhu é imoral, mau caráter, com o comportamento impuro e suspeito, que não é um asceta embora reivindique ser, não é celibato embora reivindique ser, podre no interior, corrupto, depravado. Ele ouve: 'O bhikkhu fulano de tal, realizando por si mesmo através do conhecimento direto, aqui e agora, entra e permanece na libertação da mente e libertação através da sabedoria que são imaculadas com a destruição de todas as impurezas.' Então não lhe ocorre: 'Quando eu também, realizando por mim mesmo através do conhecimento direto, aqui e agora, entrarei e permanecerei na libertação da mente e libertação através da sabedoria que são imaculadas com a destruição de todas as impurezas?' Essa é chamada a pessoa sem anseios. </p>

<p class=Normal> (2) "E como é a pessoa cheia de anseios? Aqui, um bhikkhu é virtuoso, com bom caráter. Ele ouve: 'O bhikkhu fulano de tal, realizando por si mesmo através do conhecimento direto, aqui e agora, entra e permanece na libertação da mente e libertação através da sabedoria que são imaculadas com a destruição de todas as impurezas.' Então lhe ocorre: 'Quando eu também, realizando por mim mesmo através do conhecimento direto, aqui e agora, entrarei e permanecerei na libertação da mente e libertação através da sabedoria que são imaculadas com a destruição de todas as impurezas?' Essa é chamada a pessoa cheia de anseios. </p>

<p class=Normal> (3) "E como é a pessoa que superou os anseios? Aqui, um bhikkhu é um arahant cujas impurezas foram destruídas. Ele ouve: 'O bhikkhu fulano de tal, realizando por si mesmo através do conhecimento direto, aqui e agora, entra e permanece na libertação da mente e libertação através da sabedoria que são imaculadas com a destruição de todas as impurezas.' Então não lhe ocorre: 'Quando eu também, realizando por mim mesmo através do conhecimento direto, aqui e agora, entrarei e permanecerei na libertação da mente e libertação através da sabedoria que são imaculadas com a destruição de todas as impurezas?' Por qual razão? Porque o seu antigo anseio pela libertação desapareceu ao realizar a libertação. Essa é chamada a pessoa que superou os anseios. </p>

<p class=Normal>"Esses, bhikkhus, são os três tipos de pessoas que podem ser encontradas existindo entre os bhikkhus." </p>

