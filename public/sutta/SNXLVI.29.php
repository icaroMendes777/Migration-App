<p class=Normal> “Bhikkhus, eu não vejo nenhuma outra coisa que, quando desenvolvida e cultivada, conduz ao abandono das coisas que agrilhoam de forma tão eficaz como esta: os sete fatores da iluminação. Quais sete? O fator da iluminação da atenção plena ... o fator da iluminação da equanimidade.”</p>
 
<p class=Normal> “E como, bhikkhus, os sete fatores da iluminação são desenvolvidos e cultivados para que conduzam ao abandono das coisas que agrilhoam? Nesse caso, bhikkhus, um bhikkhu desenvolve o fator da iluminação da atenção plena, que tem como base o afastamento, desapego e cessação, que amadurece no abandono ... Ele desenvolve o fator da iluminação da equanimidade, que tem como base o afastamento, desapego e cessação, que amadurece no abandono. Quando os sete fatores da iluminação são desenvolvidos e cultivados desse modo é que eles conduzem ao abandono das coisas que agrilhoam.</p>
 
<p class=Normal> “E quais, bhikkhus, são as coisas que agrilhoam? O olho é uma coisa que agrilhoa; 
é aqui que esses grilhões, correntes e algemas surgem. O ouvido é uma coisa que agrilhoa ... 
O nariz é uma coisa que agrilhoa ... A língua é uma coisa que agrilhoa ... O corpo é uma coisa que agrilhoa ... A mente é uma coisa que agrilhoa; é aqui que esses grilhões, correntes e algemas surgem. 
Essas são chamadas de coisas que agrilhoam.”</p>



