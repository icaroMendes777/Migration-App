<p class=Normal> "Bhikkhus, cada quinzena mais de cento e cinquenta regras de treinamento são recitadas; membros de clãs que desejam o seu próprio benefício as adotam para treinamento. Elas estão todas contidas dentro dos três treinamentos. Quais três? O treinamento na virtude superior, o treinamento na mente superior e o treinamento na sabedoria superior. Esses são os três treinamentos em que tudo isso está incluído. </p>

<p class=Normal><a name=R1></a>"Aqui, bhikkhus, um bhikkhu cumpre o comportamento virtuoso, mas cultiva a concentração e a sabedoria apenas de modo parcial. Ele transgride as regras de treinamento menores e se reabilita. [<a href="#N1">1</a>] Por que razão? Porque eu não digo que ele seja incapaz disso. Mas com relação às regras que são fundamentais para a vida santa, em conformidade com a vida santa, o seu comportamento é constante e estável. Tendo adotado as regras de treinamento, ele treina adotando os preceitos de virtude. Com a completa destruição de três grilhões, ele entrou na correnteza, não mais destinado aos mundos inferiores, com o destino fixo, ele tem a iluminação como destino. </p>

<p class=Normal>"Outro bhikkhu cumpre o comportamento virtuoso, mas cultiva a concentração e a sabedoria apenas de modo parcial. Ele transgride as regras de treinamento menores e se reabilita. Por que razão? Porque eu não digo que ele seja incapaz disso. Mas com relação às regras que são fundamentais para a vida santa, em conformidade com a vida santa, o seu comportamento é constante e estável. Tendo adotado as regras de treinamento, ele treina adotando os preceitos de virtude. Com a completa destruição de três grilhões, e com o enfraquecimento da cobiça, raiva e delusão, ele é aquele que retorna uma vez, destinado a renascer neste mundo somente mais uma vez para então dar um fim ao sofrimento. </p>

<p class=Normal>"Outro bhikkhu cumpre o comportamento virtuoso e a concentração, mas cultiva a sabedoria apenas de modo parcial. Ele transgride as regras de treinamento menores e se reabilita. Por que razão? Porque eu não digo que ele seja incapaz disso. Mas com relação às regras que são fundamentais para a vida santa, em conformidade com a vida santa, o seu comportamento é constante e estável. Tendo adotado as regras de treinamento, ele treina adotando os preceitos de virtude. Com a completa destruição dos cinco primeiros grilhões ele é um daqueles que não retorna, renascendo em um dos mundos de Brahma para lá realizar o parinibbana, nunca mais retornando desse mundo. </p>

<p class=Normal>"Outro bhikkhu cumpre o comportamento virtuoso, a concentração e a sabedoria. Ele transgride as regras de treinamento menores e se reabilita. Por que razão? Porque eu não digo que ele seja incapaz disso. Mas com relação às regras que são fundamentais para a vida santa, em conformidade com a vida santa, o seu comportamento é constante e estável. Tendo adotado as regras de treinamento, ele treina adotando os preceitos de virtude. Com a completa destruição das impurezas mentais, ele permanece em um estado livre de impurezas com a libertação da mente e a libertação através da sabedoria, tendo conhecido e manifestado isso para ele mesmo no aqui e agora. </p>

<p class=Normal>"Bhikkhus, assim aquele que cultiva parcialmente obtém êxito parcial; quem cultiva por completo obtém êxito completo. Essas regras de treinamento, eu digo, não são infrutíferas."</p>

