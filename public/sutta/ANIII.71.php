<p class=Normal style='margin-left:.75in'>

 

<p class=Normal>Certa ocasião o Abençoado estava em Savatthi, no Bosque de Jeta, no Parque de Anathapindika. Então o errante Channa foi até o Ven. Ananda e depois da troca de saudações corteses e amigáveis ele sentou a um lado e disse: “Você também, amigo Ananda, defende o abandono da paixão? Você também defende o abandono da raiva? Você também defende o abandono da delusão?”  </p>

 

<p class=Normal> “Sim, amigo, nós defendemos o abandono da paixão, o abandono da raiva e o abandono da delusão.” </p>

 

<p class=Normal> “Mas, amigo Ananda, vendo quais desvantagens na paixão você defende o abandono da paixão? Vendo quais desvantagens na raiva você defende o abandono da raiva? Vendo quais desvantagens na delusão você defende o abandono da delusão?” </p>

 

<p class=Normal> “Uma pessoa apaixonada, a sua mente atada, subjugada pela paixão, intenciona o seu próprio dano, intenciona o dano dos outros, intenciona o dano de ambos. Ela também experimenta aflição e angústia na mente. Mas tendo abandonado a paixão, ela não intenciona o seu próprio dano, ela não intenciona o dano dos outros, ela não intenciona o dano de ambos. Ela não experimenta aflição e angústia na mente. </p>

 

<p class=Normal> “Uma pessoa apaixonada, a sua mente atada, subjugada pela paixão, pratica ações impróprias com o corpo, ações impróprias com a linguagem, ações impróprias com a mente. Mas tendo abandonado a paixão ela não pratica ações impróprias com o corpo, ações impróprias com a linguagem, ações impróprias com a mente. </p>

 

<p class=Normal> “Uma pessoa apaixonada, a sua mente atada, subjugada pela paixão, não compreende, como na verdade é, aquilo que lhe traz benefício, aquilo que traz benefício para os outros, aquilo que traz benefício para ambos. Mas tendo abandonado a paixão, ela compreende, como na verdade é, aquilo que lhe traz benefício, aquilo que traz benefício para os outros, aquilo que traz benefício para ambos. </p>

 

<p class=Normal> “A paixão, meu amigo, deixa você cego, faz com que você perca a visão, faz de você um ignorante. Ela conduz à cessação da sabedoria, conduz a problemas, e não conduz a nibbana. </p>

 

<p class=Normal> “Uma pessoa enraivecida, a sua mente atada, subjugada pela raiva, intenciona o seu próprio dano, intenciona o dano dos outros, intenciona o dano de ambos. Ela também experimenta aflição e angústia na mente. Mas tendo abandonado a raiva, ela não intenciona o seu próprio dano, ela não intenciona o dano dos outros, ela não intenciona o dano de ambos. Ela não experimenta aflição e angústia na mente. </p>

 

<p class=Normal> “Uma pessoa enraivecida, a sua mente atada, subjugada pela raiva, pratica ações impróprias com o corpo, ações impróprias com a linguagem, ações impróprias com a mente. Mas tendo abandonado a raiva ela não pratica ações impróprias com o corpo, ações impróprias com a linguagem, ações impróprias com a mente. </p>

 

<p class=Normal> “Uma pessoa enraivecida, a sua mente atada, subjugada pela raiva, não compreende, como na verdade é, aquilo que lhe traz benefício, aquilo que traz benefício para os outros, aquilo que traz benefício para ambos. Mas tendo abandonado a raiva, ela compreende, como na verdade é, aquilo que lhe traz benefício, aquilo que traz benefício para os outros, aquilo que traz benefício para ambos. </p>

 

<p class=Normal> “A raiva, meu amigo, deixa você cego, faz com que você perca a visão, faz de você um ignorante. Ela conduz à cessação da sabedoria, conduz a problemas, e não conduz a nibbana. </p>

 

<p class=Normal> “Uma pessoa deludida, a sua mente atada, subjugada pela delusão, intenciona o seu próprio dano, intenciona o dano dos outros, intenciona o dano de ambos. Ela também experimenta aflição e angústia na mente. Mas tendo abandonado a delusão, ela não intenciona o seu próprio dano, ela não intenciona o dano dos outros, ela não intenciona o dano de ambos. Ela não experimenta aflição e angústia na mente. </p>

 

<p class=Normal> “Uma pessoa deludida, a sua mente atada, subjugada pela delusão, pratica ações impróprias com o corpo, ações impróprias com a linguagem, ações impróprias com a mente. Mas tendo abandonado a delusão ela não pratica ações impróprias com o corpo, ações impróprias com a linguagem, ações impróprias com a mente. </p>

 

<p class=Normal> “Uma pessoa deludida, a sua mente atada, subjugada pela delusão, não compreende, como na verdade é, aquilo que lhe traz benefício, aquilo que traz benefício para os outros, aquilo que traz benefício para ambos. Mas tendo abandonado a delusão, ela compreende, como na verdade é, aquilo que lhe traz benefício, aquilo que traz benefício para os outros, aquilo que traz benefício para ambos. </p>

 

<p class=Normal> “A delusão, meu amigo, deixa você cego, faz com que você perca a visão, faz de você um ignorante. Ela conduz à cessação da sabedoria, conduz a problemas, e não conduz a nibbana. </p>

 

<p class=Normal> “Vendo essas desvantagens na paixão nós defendemos o abandono da paixão. Vendo essas desvantagens na raiva nós defendemos o abandono da raiva. Vendo essas desvantagens na delusão nós defendemos o abandono da delusão.” </p>

 

<p class=Normal> “Mas, amigo, há um caminho, um meio, para abandonar a paixão, a raiva e a delusão?” </p>

 

<p class=Normal> “Sim, meu amigo, há um caminho, um meio, para abandonar a paixão, a raiva e a delusão.” </p>

 

<p class=Normal> “E qual é esse caminho, meu amigo, qual é esse meio, para abandonar a paixão, a raiva e a delusão?” </p>

 

<p class=Normal> “Exatamente este nobre caminho óctuplo: entendimento correto, pensamento correto, linguagem correta, ação correta, modo de vida correto, esforço correto, atenção plena correta, concentração correta. Esse é o caminho, esse é o meio para abandonar a paixão, a raiva e a delusão.” </p>

 

<p class=Normal> “Esse é um caminho auspicioso, amigo Ananda, é um caminho auspicioso para o abandono daquela paixão, raiva e delusão – isso é o suficiente para se esforçar pelo objetivo com diligência.” </p>

 

