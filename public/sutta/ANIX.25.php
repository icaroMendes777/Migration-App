<p class=Normal> "Bhikkhus, quando a sabedoria de um bhikkhu está bem consolidada, ele é capaz de afirmar: 'O nascimento foi destruído, a vida santa foi vivida, o que devia ser feito foi feito, não há mais vir a ser a nenhum estado.'</p>

<p class=Normal>"Como a sabedoria de um bhikkhu está bem consolidada? (1) A sabedoria de um bhikkhu está bem consolidada [quando ele sabe]: 'minha mente está sem cobiça.' (2) A sabedoria de um bhikkhu está bem consolidada [quando ele sabe]: 'minha mente está sem raiva.' (3) A sabedoria de um bhikkhu está bem consolidada [quando ele sabe]: 'minha mente está sem delusão.' (4) A sabedoria de um bhikkhu está bem consolidada [quando ele sabe]: 'minha mente não está sujeita à paixão.' (5) A sabedoria de um bhikkhu está bem consolidada [quando ele sabe]: 'minha mente não está sujeita à animosidade.' (6) A sabedoria de um bhikkhu está bem consolidada [quando ele sabe]: 'minha mente não está sujeita à confusão.' (7) A sabedoria de um bhikkhu está bem consolidada [quando ele sabe]: 'minha mente não está sujeita à existência no reino da esfera sensual.' (8) A sabedoria de um bhikkhu está bem consolidada [quando ele sabe]: 'minha mente não está sujeita à existência no reino da matéria sutil.' (9) A sabedoria de um bhikkhu está bem consolidada [quando ele sabe]: 'minha mente não está sujeita à existência no reino imaterial.' </p>

<p class=Normal>"Quando, bhikkhus, a sabedoria de um bhikkhu está bem consolidada, ele é capaz de afirmar: 'O nascimento foi destruído, a vida santa foi vivida, o que devia ser feito foi feito, não há mais vir a ser a nenhum estado.'" </p>

