<p class=Normal> Em Savatthi. Então um brâmane foi até o Abençoado e perguntou:</p>

<p class=Normal> "Mestre Gotama, 'todas as coisas existem'?" </p>

 <p class=Normal> "A idéia de que 'todas as coisas existem', brâmane,  é a cosmologia mais antiga." </p>

<p class=Normal>"Então 'todas as coisas não existem'?" </p>

<p class=Normal> "A idéia de que 'todas as coisas não existem' é a segunda cosmologia." </p>

<p class=Normal>"Então 'tudo é unidade'?" </p>

<p class=Normal> "A idéia de que 'tudo é unidade' é a terceira cosmologia." </p>

<p class=Normal>"Então 'tudo é pluralidade'?" </p>

<p class=Normal>"A idéia de que 'tudo é pluralidade' é a quarta cosmologia. Evitando todos esses extremos, o Tathagata ensina o Dhamma pelo meio, da ignorância como condição, as formações volitivas [surgem]. Das formações volitivas como
condição, a consciência. Da consciência como condição, a
mentalidade-materialidade (nome e forma). Da mentalidade-materialidade (nome e forma) como condição, as seis
bases dos sentidos. Das seis bases dos sentidos como condição, o contato. Do
contato como condição, a sensação. Da sensação como condição, o desejo. Do
desejo como condição, o apego. Do apego como condição, o ser/existir. Do
ser/existir como condição, o nascimento. Do nascimento como condição, então o
envelhecimento e morte, tristeza, lamentação, dor, angústia e desespero surgem." </p>


