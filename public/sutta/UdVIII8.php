<p class=Normal>Assim ouvi. Em certa ocasião o Abençoado estava em Savatthi, no palácio da mãe de Migara, no Parque do Oriente. Naquela ocasião, um neto querido e amado de Visakha, a mãe de Migara, havia falecido. Então, Visakha, a mãe de Migara - com suas roupas e cabelos molhados (de lágrimas) - foi até o Abençoado no meio do dia e, ao chegar, tendo o homenageado, ela sentou a um lado. Enquanto ela estava sentada ali o Abençoado disse para Visakha: "Por qual razão você veio aqui, Visakha - com suas roupas e cabelos molhados - no meio do dia?”</p>



<p class=Normal>Quando isto foi dito, ela respondeu ao Abençoado: "Meu querido e amado neto faleceu. Esta é a razão pela qual vim até aqui - com minhas roupas e cabelos molhados - no meio do dia.”</p>



<p class=Normal>"Visakha, você gostaria de ter um número de filhos e netos tão grande quanto o de pessoas em Savatthi?”</p>



<p class=Normal>"Sim, senhor, eu gostaria de ter um número de filhos e netos tão grande quanto o de pessoas em Savatthi.”</p>



<p class=Normal>"Mas quantas pessoas morrem em Savatthi no decorrer de um dia?”</p>



<p class=Normal>”Venerável senhor, há dias em que dez pessoas morrem em Savatthi. Há dias em que nove pessoas morrem em Savatthi. Há dias em que oito pessoas morrem em Savatthi. Há dias em que sete pessoas morrem em Savatthi. Há dias em que seis pessoas morrem em Savatthi. Há dias em que cinco pessoas morrem em Savatthi. Há dias em que quatro pessoas morrem em Savatthi. Há dias em que três pessoas morrem em Savatthi. Há dias em que duas pessoas morrem em Savatthi. Há dias em que uma pessoa morre em Savatthi. Savatthi nunca está livre da morte de pessoas.”</p>



<p class=Normal>"Então o que você pensa, Visakha? Haveria um dia em que você não se encontraria com suas roupas e cabelos molhados?”</p>



<p class=Normal>"Não, senhor. Basta esta coisa de ter tantos filhos e netos.”</p>



<p class=Normal>"Visakha, Aqueles que possuem uma centena de entes queridos têm uma centena de sofrimentos. Aqueles que possuem nove dezenas de entes queridos têm nove dezenas de sofrimentos. Aqueles que possuem oito dezenas de entes queridos têm oito dezenas de sofrimentos. Aqueles que possuem sete dezenas de entes queridos têm sete dezenas de sofrimentos. Aqueles que possuem seis dezenas de entes queridos têm seis dezenas de sofrimentos. Aqueles que possuem cinco dezenas de entes queridos têm cinco dezenas de sofrimentos. Aqueles que possuem quatro dezenas de entes queridos têm quatro dezenas de sofrimentos. Aqueles que possuem três dezenas de entes queridos têm três dezenas de sofrimentos. Aqueles que possuem duas dezenas de entes queridos têm duas dezenas de sofrimentos. Aqueles que possuem uma dezena de entes queridos têm uma dezena de sofrimentos. Aqueles que possuem nove entes queridos têm nove sofrimentos. Aqueles que possuem oito entes queridos têm oito sofrimentos. Aqueles que possuem sete entes queridos têm sete sofrimentos. Aqueles que possuem seis entes queridos têm seis sofrimentos. Aqueles que possuem cinco entes queridos têm cinco sofrimentos. Aqueles que possuem quatro entes queridos têm quatro sofrimentos. Aqueles que possuem três entes queridos têm três sofrimentos. Aqueles que possuem dois entes queridos têm dois sofrimentos. Aqueles que possuem um ente queridos têm um sofrimentos. Aqueles que não possuem entes queridos não tem sofrimento. Eles estão livres da tristeza, são puros e serenos.”</p>

 

<p class=Normal>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p>



	<p class=Normal style='margin-left:.75in'>"As tristezas, lamentações,<br>

	os muitos tipos de sofrimentos do mundo, <br>

	dependem daquilo que é querido.<br>

	Eles não existem quando não há o que seja querido.</p>

	

	<p class=Normal style='margin-left:.75in'>Portanto são felizes e livres de sofrimento<br>

	aqueles que não possuem nada que lhes seja querido<br> 

	em qualquer lugar deste mundo.</p>

	

	<p class=Normal style='margin-left:.75in'>Aquele que aspira à pureza e felicidade<br>

	não deve possuir nada querido<br>

	em qualquer lugar deste mundo.”</p>

