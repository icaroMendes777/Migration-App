<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em certa
ocasião, o Abençoado estava<span style="mso-spacerun: yes">  </span>entre os Sakyas
em Kapilavatthu, no Parque de Nigrodha. Então ao amanhecer o Abençoado se
vestiu e tomando a tigela e o manto externo foi até a residência da senhora
Sakya Kaligodha sentando num assento que havia sido preparado. Então, a senhora
Sakya Kaligodha foi até o Abençoado e depois de cumprimentá-lo sentou a um lado
e o Abençoado então lhe disse:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Godha, uma nobre discípula que possui quatro coisas entrou
na correnteza, não mais destinada aos mundos inferiores, com o destino fixo,
ela tem a iluminação como destino. Quais quatro?<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>"Aqui,
Godha, uma nobre discípula possui perfeita claridade, serenidade e confiança no Buda assim: ‘O Abençoado é um arahant,
perfeitamente iluminado, consumado no verdadeiro conhecimento e conduta,
bem-aventurado, conhecedor dos mundos, um líder insuperável de pessoas
preparadas para serem treinadas, mestre de devas e humanos, desperto,
sublime.’</p> 
<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>"Ela possui perfeita claridade, serenidade e confiança no Dhamma assim: ‘O Dhamma é bem proclamado pelo
Abençoado, visível no aqui e agora, com efeito imediato, que convida ao exame,
que conduz para adiante, para ser experimentado pelos sábios por eles mesmos.’</p>
<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>"Ela possui perfeita claridade, serenidade e confiança na Sangha assim: ‘A Sangha dos discípulos do Abençoado
pratica o bom caminho, pratica o caminho reto, pratica o caminho verdadeiro,
pratica o caminho adequado, isto é, os quatro pares de pessoas, os oito tipos
de indivíduos; esta Sangha dos discípulos do Abençoado é merecedora de dádivas,
merecedora de hospitalidade, merecedora de oferendas, merecedora de saudações
com reverência, um campo inigualável de mérito para o mundo.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>"Ela permanece em casa com uma mente desprovida da mácula da avareza, espontaneamente
generosa, mão aberta, que se delicia com a renúncia, devotada à caridade,
deliciando-se em dar e compartir.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Uma 
nobre discípula, Godha, que possui essas quatro coisas entrou na correnteza,
não mais destinada aos mundos inferiores, com o destino fixo, ela tem a
iluminação como destino.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, quanto a esses quatro fatores daquele que entrou na correnteza, que
foram ensinados pelo Abençoado, essas coisas existem em mim e eu vivo em
conformidade com essas coisas. Pois, venerável senhor, eu tenho perfeita claridade, serenidade e confiança no Buda ... no Dhamma ... na Sangha. Além disso, qualquer coisa que
minha família tenha, que seja adequado para dar, tudo isso eu comparto sem
reservas entre aqueles que são virtuosos e de bom caráter.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“É um ganho
para você, Godha! Muito bem ganho por você, Godha! Você declarou o fruto
daquele que entra na correnteza.”<o:p></o:p></span></p>


