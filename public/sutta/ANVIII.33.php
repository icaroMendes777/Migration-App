<p class=Normal> "Bhikkhus, há esses oito motivos para a generosidade. Quais oito? (1) Alguém dá por desejo. (2) Alguém dá por raiva. (3) Alguém dá por delusão. (4) Alguém dá por medo. (5) Alguém dá com o pensamento: 'A generosidade foi praticada antes pelo meu pai e pelos meus ancestrais; eu não devo abandonar esse antigo hábito familiar'. (6) Alguém dá com o pensamento: 'Tendo dado essa oferenda, com a dissolução do corpo, após a morte, renascerei num destino feliz, no paraíso'. (7) Alguém dá com o pensamento: 'Quando dou essa oferenda minha mente fica satisfeita, clara, com serena confiança'. (8) A oferenda é dada com o propósito de ornamentar a mente, equipar a mente". [<a href="#N1">1</a>]
