<p class=Normal> Certa ocasião o Venerável Ananda e o Venerável Kamabhu estavam em Kosambi no Parque de Ghosita. Então ao anoitecer o Venerável Kamabhu saiu do seu isolamento e foi até o Venerável Ananda e ambos se cumprimentaram. Depois da troca de saudações amigáveis e corteses ele sentou a um lado e disse: </p>

<p class=Normal> “Como é, amigo Ananda, o olho é o grilhão das formas ou são as formas o grilhão do olho? O ouvido ... O nariz ... A língua ... O corpo ... A mente é o grilhão dos objetos mentais ou são os objetos mentais o grilhão da mente?” </p>

<p class=Normal> “Amigo Kamabhu, o olho não é o grilhão das formas nem são as formas o grilhão do olho, mas melhor dito, o desejo e a cobiça que surge na dependência de ambos: esse é o grilhão. O ouvido não é o grilhão dos sons ... O nariz não é o grilhão dos aromas ... A língua não é o grilhão dos sabores ... O corpo não é o grilhão das sensações tangíveis ... A mente não é o grilhão dos objetos mentais, nem os objetos mentais são o grilhão da mente, mas melhor dito, o desejo e a cobiça que surge na dependência de ambos: esse é o grilhão. </p>

<p class=Normal> “Suponha. amigo, que um boi preto e um boi branco fossem unidos por um único arreio ou canga. Alguém falaria corretamente se dissesse: ‘O boi preto é o grilhão do boi branco, o boi branco é o grilhão do boi preto’?” </p>

<p class=Normal> “Não, amigo. O boi preto não é o grilhão do boi branco, nem o boi branco é o grilhão do preto, mas melhor dito, o arreio ou canga pelo qual eles estão unidos: esse é o grilhão." </p>

<p class=Normal> “Da mesma forma, amigo, o olho não é o grilhão das formas, nem são as formas o grilhão do olho, mas melhor dito, o desejo e a cobiça que surge na dependência de ambos: esse é o grilhão. O ouvido não é o grilhão dos sons ... O nariz não é o grilhão dos aromas ... A língua não é o grilhão dos sabores ... O corpo não é o grilhão das sensações tangíveis ... A mente não é o grilhão dos objetos mentais, nem os objetos mentais são o grilhão da mente, mas melhor dito, o desejo e a cobiça que surge na dependência de ambos: esse é o grilhão." </p>

