<p class=Normal> “Bhikkhus, esses cinco obstáculos causam a cegueira, causam a falta de visão, causam a ausência de conhecimento, prejudicam a sabedoria, tendem para a aflição, afastam de Nibbana. Quais cinco? O obstáculo do desejo sensual causa a cegueira ... o obstáculo da má vontade causa a cegueira ... O obstáculo da preguiça e torpor ... O obstáculo da inquietação e ansiedade ... O obstáculo da dúvida causa a cegueira ... afasta de Nibbana. Esses cinco obstáculos causam a cegueira, causam a falta de visão, causam a ausência de conhecimento, prejudicam a sabedoria, tendem para a aflição, afastam de Nibbana.</p>
<p class=Normal> “Esses sete fatores da iluminação, bhikkhus, produzem a visão, produzem o conhecimento, promovem o incremento da sabedoria, livram da aflição, conduzem a Nibbana. Quais sete? O fator da iluminação da atenção plena produz a visão ... O fator da iluminação da equanimidade produz a visão ... conduz a Nibbana. Esses sete fatores da iluminação produzem a visão, produzem o conhecimento, promovem o incremento da sabedoria, livram da aflição, conduzem a Nibbana.”</p>

