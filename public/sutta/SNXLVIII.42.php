<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. Então,<span style="mso-spacerun: yes">  </span>o brâmane Unnabha foi
até o Abençoado e ambos se cumprimentaram. Quando a conversa cortês e amigável havia
terminado, ele sentou a um lado e disse para o Abençoado:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mestre
Gotama, essas cinco faculdades possuem cada uma um campo separado, um domínio
separado e não experimentam o campo e o domínio das demais. Quais cinco? A
faculdade do olho, a faculdade do ouvido, a faculdade do nariz, a faculdade da
língua, a faculdade do corpo. [<a href="#N1">1</a>] <a name=R1></a>Agora,
Mestre Gotama, essas cinco faculdades, cada uma possuindo um campo separado, um
domínio separado e não experimentando o campo e o domínio das demais, a que
elas recorrem, o que é que experimenta os seus campos e os seus domínios?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Brâmane,
estas cinco faculdades possuem cada uma um campo separado, um domínio separado
e não experimentam o campo e o domínio das demais, isto é, a faculdade do olho,
a faculdade do ouvido, a faculdade do nariz, a faculdade da língua e a
faculdade do corpo. Agora, essas cinco faculdades, cada uma possuindo um campo
separado, um domínio separado e não experimentando o campo e o domínio das
demais, recorrem à mente e a mente experimenta os seus campos e os seus
domínios.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas,
Mestre Gotama, a que recorre a mente?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“A mente,
brâmane, recorre à atenção plena.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas,
Mestre Gotama, a que recorre a atenção plena?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“A atenção
plena, brâmane, recorre à libertação.” [<a href="#N2">2</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Mas, Mestre Gotama, a que recorre a libertação?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“A
libertação, brâmane, recorre a Nibbana.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas,
Mestre Gotama, a que recorre Nibbana?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Você levou
essa série de questões longe demais, brâmane, você não foi capaz de compreender
o limite para as questões. Pois a vida santa, brâmane, se unifica em Nibbana,
culmina em Nibbana, termina em Nibbana.” [<a href="#N3">3</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Então, o brâmane Unnabha, estando satisfeito e contente com as palavras do
Abençoado, levantou-se do seu assento e depois de homenagear o Abençoado,
mantendo-o à sua direita, partiu. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, não
muito tempo depois do brâmane Unnabha haver partido, o Abençoado se dirigiu aos
bhikkhus desta forma:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus, suponham
que numa casa ou salão com uma cumeeira, em frente a uma janela voltada para o
Leste, o sol estivesse nascendo. Quando os seus raios entrassem pela janela,
onde eles pousariam?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Na parede
do lado Oeste, venerável senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Da mesma
forma bhikkhus, o brâmane Unnabha ganhou confiança no Tathagata que é firme,
com raízes profundas, estabelecida e estável. Ela não pode ser removida por
qualquer contemplativo ou brâmane, ou deva, ou Mara, ou Brahma, ou qualquer
outro no mundo. Se, bhikkhus, o brâmane Unnabha morresse agora, não haveria
nenhum grilhão atado pelo qual ele pudesse novamente retornar a este mundo.”<o:p></o:p></span></p>


