<p class=Normal><a name=N15></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[15] <i>Abhibhu</i>. MA diz que este termo é
uma designação para o mundo não perceptivo, assim chamado porque ele conquista,
(<i>abhibhavati</i>), os quatro agregados imateriais. Essa identificação soa
artificial especialmente por que a palavra <i>abhibhu</i> é um nome masculino
singular. No <a href="MN49.php#T5">MN 49.5</a> a palavra aparece como Baka, a
reivindicação de Brahma da hegemonia teocrática, no entanto, MA rejeita a
identificação de <i>Abhibhu </i>com Brahma neste caso por ser uma redundância.
[<a href="#R15">Retorna</a>]<o:p></o:p></span></p>

<p class=Normal><a name=N16></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[16] Esta e as próximas três seções tratam da
concepção relacionada com os quatro mundos imateriais da existência – as
contrapartes cosmológicas das quatro realizações meditativas imateriais (jhanas
imateriais). No verso 18 a divisão da concepção por meio dos planos de
existência está completa. [<a href="#R16">Retorna</a>]<o:p></o:p></span></p>

<p class=Normal><a name=N17></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[17] Nestas quatro seções os fenômenos que
compreendem a identidade são considerados como objetos da percepção
classificados nas quatro categorias do visto, ouvido, sentido e conscientizado.
Neste caso, sentido, (<i>muta</i>), significa as experiências do olfato, paladar
e toque, e conscientizado, (<i>viññata</i>), as experiências de introspecção,
pensamento abstrato e imaginação. Os objetos da percepção são “concebidos”
quando percebidos como “meu” ou “eu” ou de formas que geram o desejo,
presunção e idéias. [<a href="#R17">Retorna</a>]<o:p></o:p></span></p>

<p class=Normal><a name=N18></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[18] Nesta seção e na seguinte, os fenômenos
que compreendem a identidade são tratados de duas formas -<span
style='mso-spacerun:yes'>  </span>unidade e diversidade. A ênfase na unidade, (<i>ekatta</i>),
MA nos informa, é característica daquele que alcança os jhanas, nos quais a
mente ocorre de modo único num único objeto. A ênfase na diversidade, (<i>nanatta</i>),
prevalece para aqueles que não alcançaram os jhanas faltando-lhes a
impressionante experiência unificadora dos jhanas. As concepções que enfatizam
a diversidade ganham expressão nas filosofias do pluralismo, aquelas que
enfatizam a unidade são típicas das filosofias monísticas. [<a href="#R18">Retorna</a>]<o:p></o:p></span></p>

<p class=Normal><a name=N19></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[19] Nesta seção todos os fenômenos da
identidade são reunidos e mostrados de forma única. Essa idéia de totalidade
pode constituir a base para as filosofias to tipo panteísta ou monística,
dependendo da relação postulada entre o eu e o todo. [<a href="#R19">Retorna</a>]<o:p></o:p></span></p>

<p class=Normal><a name=N20></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[20] MA entende que “Nibbana” neste caso se
refere aos cinco tipos de “Nibbana aqui e agora” incluídos dentre os sessenta e
dois tipos de entendimento incorreto explicados no Brahmajala Sutta (<a
href="DN1.php#T3_19">DN 1.3.19-25</a>), isto é, Nibbana identificado com o gozo
pleno dos prazeres sensuais ou com os quatro jhanas. Desfrutando desse estado,
ou ansiando por ele, ele o concebe com base no desejo. Orgulhando-se por tê-lo
alcançado, ele o concebe com base na presunção. Considerando que esse Nibbana
imaginário é permanente, etc., ele o concebe com base em idéias. [<a href="#R20">Retorna</a>]<o:p></o:p></span></p>

<p class=Normal><a name=N21></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[21] O <i>sekha</i>, o discípulo no
treinamento superior, é aquele que alcançou algum dos três níveis inferiores de
iluminação – que entrou na correnteza, que retorna uma vez, que não retorna –
mas que ainda precisa treinar mais para alcançar o objetivo último, o estado de
arahant, a suprema segurança contra o cativeiro. O <a href="MN53.php">MN 53</a>
explica o treinamento que ele deve seguir. O arahant algumas vezes é descrito
como <i>asekha</i>, aquele que está além do treinamento, no sentido de que ele
completou o treinamento do Nobre Caminho Óctuplo. [<a href="#R21">Retorna</a>]<o:p></o:p></span></p>

<p class=Normal><a name=N22></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[22] Deve ser observado que, enquanto da
pessoa comum se diz que ela percebe cada uma dessas bases, daquele no
treinamento superior se diz que ele as conhece diretamente, (<i>abhijanati</i>).
MA explica que ele as conhece com o conhecimento diferenciado, as conhece de
acordo com a sua própria natureza como impermanentes, insatisfatórias e não-eu.
[<a href="#R22">Retorna</a>]<o:p></o:p></span></p>

<p class=Normal><a name=N23></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[23] O discípulo no treinamento superior é
instado pelo Buda a se abster da concepção e do deleite porque as inclinações
por esses processos mentais ainda permanecem dentro dele. Com a realização do
estado de entrar na correnteza, ele erradicou o grilhão da idéia da existência
de um eu, e portanto será incapaz de conceber com base no entendimento
incorreto. Mas as contaminações do desejo e da presunção apenas são erradicadas
com o caminho do arahant, e dessa forma o <i>sekha </i>permanece vulnerável às
concepções que possam se originar destas. Enquanto o conhecimento direto, (<i>abhiñña</i>),
pertence à esfera tanto do <i>sekha</i> como do arahant, a completa
compreensão, (<i>pariñña</i>), é da esfera exclusiva do arahant, visto que
envolve o completo abandono de todas as impurezas. [<a href="#R23">Retorna</a>]<o:p></o:p></span></p>

<p class=Normal><a name=N24></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[24] Esta é a descrição padrão de um arahant
encontrada em muitos suttas. [<a href="#R24">Retorna</a>]<o:p></o:p></span></p>

<p class=Normal><a name=N25></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[25] Quando a ignorância foi abolida através
da realização da compreensão completa, as inclinações mais sutis para o desejo
e para a presunção também são erradicadas. Assim, o arahant não mais se ocupa
com a concepção e o deleite. [<a href="#R25">Retorna</a>]<o:p></o:p></span></p>

<p class=Normal><a name=N26></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[26] Esta seção e as duas seguintes são
incluídas para mostrar que o arahant não concebe, não só porque ele compreendeu
completamente o objeto, mas porque ele erradicou as três raízes prejudiciais –
cobiça (ou desejo), raiva e delusão. A frase “livre da cobiça através da
destruição da cobiça” é empregada para enfatizar que o arahant não está apenas
temporariamente sem cobiça, mas que ele destruiu a cobiça no seu nível mais
fundamental. O mesmo se aplica à raiva e delusão. [<a href="#R26">Retorna</a>]<o:p></o:p></span></p>

<p class=Normal><a name=N27></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[27] Com relação a esta palavra, que é o
epíteto que o Buda usava com mais freqüência ao referir a si próprio, veja a <a
href="../arquivo_textos_theravada/majjhima_intro.php#Tathagata">Introdução ao
Majjhima Nikaya</a>.[<a href="#R27">Retorna</a>]<o:p></o:p></span></p>

<p class=Normal><a name=N28></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[28] <i>Pariññatantam tathagatassa</i>. MA explica:
compreendeu completamente até a conclusão, compreendeu completamente até o
limite, compreendeu completamente sem restar nada. Isto explica que enquanto os
Budas e os discípulos arahants se assemelham no abandono de todas as impurezas,
há, no entanto, uma distinção na abrangência da sua completa compreensão:
enquanto os discípulos podem alcançar Nibbana depois de compreenderem com o
insight apenas um número limitado de formações, os Budas compreendem
completamente todas as formações sem exceção. [<a href="#R28">Retorna</a>]<o:p></o:p></span></p>

<p class=Normal><a name=N29></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[29] Esta sentença proporciona um enunciado
altamente comprimido da fórmula da origem dependente, (<i>paticca samuppada</i>),
em geral explicada com doze fatores (tal qual no <a href="MN38.php">MN 38</a>).
Conforme interpretado no MA, “deleite” é o desejo da vida passada que resultou
no “sofrimento” dos cinco agregados da vida presente, “ser/existir” é o aspecto
determinativo de kamma na vida presente, que causará o futuro nascimento
seguido de envelhecimento e morte. Este trecho mostra que a causa da eliminação
da concepção no Buda foi a compreensão da origem dependente na noite da sua
iluminação. A menção do “deleite”, (<i>nandi</i>), como a raiz do sofrimento,
estabelece uma conexão com o título do sutta; além disso, no enunciado anterior,
em que a pessoa comum se delicia com a terra, etc., mostra que o sofrimento é a
conseqüência última do deleite. [<a href="#R29">Retorna</a>]<o:p></o:p></span></p>

<p class=Normal><a name=N30></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[30] MA explica a seqüência de idéias da
seguinte forma: O Tathagata não concebe a terra e não se delicia com a terra
porque ele compreendeu que o deleite é a raiz do sofrimento. Além disso, por
ter compreendido a origem dependente, ele abandonou por completo o desejo, aqui
chamado de “deleite” e despertou para a suprema perfeita iluminação. Como resultado
ele não concebe a terra ou se delicia com a terra. [<a href="#R30">Retorna</a>]<o:p></o:p></span></p>

<p class=Normal><a name=N31></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[31] Os bhikkhus não ficaram contentes com as
palavras do Buda, aparentemente porque o discurso acabou atingindo fundo os
Brâmanes no seu orgulho e talvez nas suas idéias brâmanes residuais. Mais
tarde, relata MA, quando o orgulho deles havia diminuído, o Buda explicou para
esses mesmos bhikkhus o Gotamaka Sutta (AN 3:12) durante o qual
todos alcançaram o estado de arahant. [<a href="#R31">Retorna</a>]</span><b
