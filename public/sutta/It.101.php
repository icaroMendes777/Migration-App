<p class=Normal> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: "Bhikkhus, há essas quatro insignificâncias, ganhas facilmente e não passíveis de crítica. Quais quatro? Um manto feito de trapos é uma insignificância ganho facilmente e não passível de crítica. Comida esmolada é uma insignificância ganha facilmente e não passível de crítica. A sombra de uma árvore é uma insignificância ganha facilmente e não passível de crítica. Urina fermentada é uma insignificância entre os medicamentos, ganha facilmente e não passível de crítica. Essas, bhikkhus, são as quatro insignificâncias, ganhas facilmente e não passíveis de crítica. Quando um bhikkhu está satisfeito com aquilo que é uma insignificância, ganha facilmente, eu digo, que ele possui um dos fatores da vida contemplativa."</p>

<p class=Normal style='margin-left:.75in'> Quando alguém está satisfeito<br> com aquilo que não é passível de crítica, <br>
uma insignificância ganha facilmente; <br>
quando a sua mente não está estressada<br>
por uma moradia, <br>
mantos, bebidas, e comida, <br>
ele não enfrenta nenhum obstáculo.</p>

<p class=Normal style='margin-left:.75in'> Essas qualidades, corretamente ditas,<br>
fazem parte da vida contemplativa, <br>
são obtidas por um bhikkhu<br>
que está satisfeito e diligente.</p>



