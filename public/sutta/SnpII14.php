<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim ouvi.
Em certa ocasião o Abençoado estava<span style='mso-spacerun:yes'>  </span>em
Savatthi no Bosque de Jeta, no Parque de Anathapindika. Então o discípulo leigo
Dhammika com outros quinhentos discípulos leigos foi até o Abençoado e depois
de cumprimentá-lo sentou a um lado e disse: </span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Eu pergunto a Gotama </span>[<a
href="#N1"><span lang=PT-BR style='mso-ansi-language:PT-BR'>1</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=T1></a>que possui vasta
sabedoria o seguinte: De que maneira um discípulo é virtuoso através da ação -
ambos o discípulo que adotou a vida santa e o discípulo que é um chefe de
família? Pois você claramente entende o comportamento do mundo com os seus
devas, e a libertação final </span>[<a href="#N2"><span lang=PT-BR
style='mso-ansi-language:PT-BR'>2</span></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>]. <a name=T2></a>Não existe ninguém igual a você que tenha a habilidade
de ver o que é profundo. Você é um Perfeitamente Iluminado. Tendo investigado
todo o conhecimento e tendo compaixão pelos seres você anunciou o Dhamma,
revelou o que estava escondido, de visão abrangente, imaculado, você ilumina
todos os mundos. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Este Dhamma, sutil e
que sacia, ensinado de maneira tão clara por você, Mestre, é isso que todos
queremos ouvir. Tendo sido questionado, Iluminado, diga-nos (a resposta ).
Todos esses bhikkhus e também os discípulos leigos vieram para ouvir a verdade,
permita que eles ouçam o Dhamma que foi despertado (<i style='mso-bidi-font-style:
normal'>anubuddham</i>) pelo Abençoado, como os devas ouvem as palavras de
Vasava.&quot; [</span><a href="#N3"><span lang=PT-BR style='mso-ansi-language:
PT-BR'>3</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>] <a
name=T3></a><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(O Mestre:) &quot;Ouçam-me,
bhikkhus, eu lhes ensinarei a prática ascética (<i style='mso-bidi-font-style:
normal'>dhamma dhutam</i>), o modo de vida adequado, para aqueles que adotaram
a vida santa. Vocês todos lembrem-se disso. Aquele que tem como objetivo o que
é bom e aquele que é judicioso deveria praticá-lo. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Um bhikkhu não deve esmolar
comida nas horas inadequadas mas deve ir ao vilarejo na hora apropriada. As
tentações podem importunar um bhikkhu que esmole no horário não apropriado. Portanto
os sábios não esmolam nas horas incorretas. </span>[<a href="#N4"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>4</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>] <a name=T4></a>Visões, sons, sabores, aromas,
e contatos corporais embriagam por completo as pessoas. Deixando de lado o desejo
por esses objetos sensuais, ele pode esmolar alimentos na hora certa. Obtendo comida,
regressando sozinho e sentando-se em um lugar isolado, com atenção plena e evitando
distrações, um bhikkhu deve desenvolver autocontrole. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Se ele falar com um
discípulo leigo, ou com qualquer outra pessoa ou com outro bhikkhu, ele deve
falar acerca do excelente Dhamma, não difamando outros nem fofocando. Alguns apresentam
argumentos controversos; aqueles com pouca sabedoria nós não elogiamos; eles
são atados pelos apegos e arrastados pelas suas emoções. </span>[<a href="#N5"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>5</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>] <a name=T5></a><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Tendo ouvido e
considerado o Dhamma ensinado pelo Bem-aventurado </span>[<a href="#N6"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>6</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>]<a name=T6></a>, com sabedoria o discípulo
deve fazer uso da comida,<span style='mso-spacerun:yes'>  </span>moradia, cama,
assento, água e manter o manto asseado. Porém um bhikkhu não deve ser maculado
por (pelo apego a) essas coisas, como uma flor de lótus não é manchada pela
água. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Agora eu lhes direi
as obrigações de um leigo. Seguindo-as um discípulo leigo será virtuoso; pois
não é possível para alguém que se ocupa com a vida em família, realizar a
prática completa de um bhikkhu. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Ele não deve matar
nenhum ser vivo, nem ser a causa dele ser morto, nem ele deve aprovar que outros
matem. Ele deve evitar oprimir todos os seres vivos no mundo, quer sejam fortes
ou fracos. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Um discípulo deve
evitar tomar qualquer coisa de qualquer lugar sabendo (que pertence a outrem).
Ele não deve roubar, nem aprovar que outros roubem. Ele deve evitar o roubo
completamente. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Um homem sábio deve
evitar a luxúria como (se ele evitasse cair em) um buraco cheio de brasas
ardentes. Se não for capaz de conduzir uma vida celibatária, ele não deve
buscar a mulher do outro. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Tendo entrado numa corte
real ou numa assembléia ele não deve contar mentiras. Ele não deve dizer
mentiras nem aprovar alguém que minta. Ele deve evitar a falsidade
completamente. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Um leigo que escolheu
praticar este Dhamma não deve entregar-se ao vinho, álcool e outros embriagantes.
Ele não deve ingeri-los nem encorajar que alguém o faça; percebendo que isso
causa a negligência. Através da embriaguez pessoas tolas realizam atos ruins e
prejudiciais e provocam outras pessoas negligentes a fazer o mesmo. Ele deve
evitar a embriaguez, essa circunstância para o demérito, que estupefaz a mente,
o prazer de pessoas tolas. <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:PT-BR'>Não
mate seres vivos;<br>
não tome o que não for dado;<br>
não diga mentiras;<br>
não se embriague;<br>
abstenha-se da relação sexual;<br>
não coma à noite, no horário incorreto;<br>
não use ornamentos com flores, nem use perfumes;<br>
use o chão como cama ou durma sobre uma esteira. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Esses são chamados os
oito preceitos proclamados pelo Iluminado que realizou o fim do sofrimento. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Com a mente
satisfeita observem estes preceitos de virtude no dia de observância (<i
style='mso-bidi-font-style:normal'>uposatha</i>) no décimo quarto, décimo
quinto e oitavo dias da quinzena lunar. Na manhã seguinte, com o coração puro e
uma mente alegre, um homem sábio, após observar o uposatha, deve distribuir
alimentos e bebidas adequadas para a comunidade de bhikkhus. Ele deve sustentar
sua mãe e pai como sua obrigação e dedicar-se a uma atividade dentro da lei. Um
leigo que age dessa forma diligentemente, renascerá junto dos devas luminosos.
[</span><a href="#N7"><span lang=PT-BR style='mso-ansi-language:PT-BR'>7</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>]<a name=T7></a> </span><o:p></o:p></p>

