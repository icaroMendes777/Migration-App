<p class=Normal> (1) Em certa ocasião o Abençoado estava em Savatthi, no Bosque de Jeta, no Parque de Anathapindika. Lá ele se dirigiu aos monges desta forma: “Bhikkhus” – “Venerável Senhor,” eles responderam. O Abençoado disse o seguinte: </p> 

<p class=Normal>"Bhikkhus, eu não vejo nenhuma outra forma que tanto obceca a mente de um homem como a forma de uma mulher. A forma de uma mulher obceca a mente de um homem." </p>

<p class=Normal> (2) "Bhikkhus, eu não vejo nenhum outro som que tanto obceca a mente de um homem como o som de uma mulher. O som de uma mulher obceca a mente de um homem." </p>

<p class=Normal> (3) "Bhikkhus, eu não vejo nenhum outro odor que tanto obceca a mente de um homem como o odor de uma mulher. O odor de uma mulher obceca a mente de um homem." </p>

<p class=Normal> (4) "Bhikkhus, eu não vejo nenhum outro sabor que tanto obceca a mente de um homem como o sabor de uma mulher. O sabor de uma mulher obceca a mente de um homem." </p>

<p class=Normal> (5) "Bhikkhus, eu não vejo nenhum outro toque que tanto obceca a mente de um homem como o toque de uma mulher. O toque de uma mulher obceca a mente de um homem." </p>

<p class=Normal> (6) "Bhikkhus, eu não vejo nenhuma outra forma que tanto obceca a mente de uma mulher como a forma de um homem. A forma de um homem obceca a mente de uma mulher." </p>

<p class=Normal> (7) "Bhikkhus, eu não vejo nenhum outro som que tanto obceca a mente de uma mulher como o som de um homem. O som de um homem obceca a mente de uma mulher." </p>

<p class=Normal> (8) "Bhikkhus, eu não vejo nenhum outro odor que tanto obceca a mente de uma mulher como o odor de um homem. O odor de um homem obceca a mente de uma mulher." </p>

<p class=Normal> (9) "Bhikkhus, eu não vejo nenhum outro sabor que tanto obceca a mente de uma mulher como o sabor de um homem. O sabor de um homem obceca a mente de uma mulher." </p>

<p class=Normal> (10) "Bhikkhus, eu não vejo nenhum outro toque que tanto obceca a mente de uma mulher como o toque de um homem. O toque de um homem obceca a mente de uma mulher." </p>

