<p class=Tit1 align=center style='text-align:center'><i> Adhimutta Thera</i> </Tit1>
<p class=Tit2 align=center style='text-align:center'> Versos 705 a 725</Tit2>

<p class=Normal>&nbsp;</p>
<hr size=2 width="100%" align=center>
<p class=Normal>&nbsp;</p>

<p class=Normal>No livro <i>Mindfulness, Bliss, and Beyond</i>, Ajaan Brahmavamso emprega os versos abaixo para ilustrar o grau de desapego da mente de um arahant. Ajaan Brahm diz, "o arahant tendo destruído todas as percepções de um eu não sente necessidade de ser popular. As suas mentes estão livres da necessidade de impressionar os outros. Tampouco eles experimentarão o medo, visto que não têm nada a perder. Os versos abaixo descrevem um desses destemidos, o arahant Adhimutta Thera num diálogo com alguns ladrões."</p>
<br>
<br>

<p class=Normal style='margin-left:1.0in'><b> Os ladrões:</b></p>

<p class=Normal style='margin-left:1.0in'> "Aqueles que sacrificamos,<br>
ou roubamos suas riquezas contra a sua vontade,<br>
todos estremeceram, balbuciando com medo."</p>

<p class=Normal style='margin-left:1.0in'>"Mas você nem parece estar com medo,<br>
com sua complexão brilhante;<br>
sereno e calmo, você não se lamenta<br>
quando confrontado com o maior perigo, a morte. "</p>
<br>
<p class=Normal style='margin-left:1.0in'><b> Adhimutta:</b></p>

<p class=Normal style='margin-left:1.0in'>"Não há sofrimento na mente<br>
quando a cobiça foi deixada para trás; <br>
e realmente, Chefe, todos os medos são superados<br>
naquele que para sempre cortou as suas amarras."</p>

<p class=Normal style='margin-left:1.0in'>"Quando aquilo que leva a um futuro nascimento<br>
foi destruído, e quando os fenômenos<br>
são vistos com sabedoria tal como eles realmente são, <br>
não há medo da morte; um fardo foi deposto."</p>

<p class=Normal style='margin-left:1.0in'>"A vida santa foi bem vivida por mim; <br>
o caminho foi desenvolvido até o fim. <br>
Não tenho medo da morte, assim como<br>
não temo a desolação de uma doença." <br>

<p class=Normal style='margin-left:1.0in'>"A vida santa foi bem vivida por mim; <br>
o caminho foi desenvolvido até o fim. <br>
o ser/existir é visto como banal e desprovido de qualquer sentido; <br>
eu cuspi o veneno que costumava beber."</p>

<p class=Normal style='margin-left:1.0in'>"Para aquele que alcançou a outra margem, <br>
para quem não há mais apego, que fez o que deveria ser feito, <br>
em quem as impurezas da mente cessaram, <br>
há deleite com o final conclusivo da vida, <br>
tal como alguém se delicia com a liberdade do cadafalso."</p>

<p class=Normal style='margin-left:1.0in'>"Eu realizei a mais elevada verdade, suprema; <br>
o mundo não tem mais nada para me oferecer. <br>
Na morte não há tristeza, ela traz o alívio, <br>
tal como ser resgatado de uma casa em chamas."</p>

<p class=Normal style='margin-left:1.0in'>"O grande sábio deixou claro para nós: <br>
'O que quer que aqui tenha surgido, <br>
onde quer que seja obtida o ser/existir, <br>
tudo isso não foi criado por um deus governante.'"</p>

<p class=Normal style='margin-left:1.0in'>"Quem sabe isso tal como o Buda ensinou, <br>
não se apega ao ser/existir, da mesma forma como alguém<br>
não se apegaria a uma bola de ferro incandescente."</p>

<p class=Normal style='margin-left:1.0in'>"Para mim não há 'eu fui', <br>
nem ocorre o pensamento, 'eu serei'. <br>
Quando estas formações terminam e cessam, <br>
observando-as, o que há para lamentar?" <br>

<p class=Normal style='margin-left:1.0in'>"Apenas o surgimento de puros fenômenos, <br>
a persistência de puras formações com suas causas, <br>
vistos como na verdade são, não há medo, Chefe."</p>

<p class=Normal style='margin-left:1.0in'>"Quando alguém com sabedoria olha para o mundo<br>
da mesma forma como olha para um monte de capim e gravetos, <br>
não se pode encontrar um proprietário; <br>
não haverá tristeza, porque não há 'meu'."</p>

<p class=Normal style='margin-left:1.0in'>"Estou cansado deste cadáver, e estou farto; <br>
o ser/existir perdeu seu encanto, e não haverá<br>
nenhum outro cadáver quando este corpo sofrer a dissolução."</p>

<p class=Normal style='margin-left:1.0in'>"Façam como quiserem, façam o que vocês têm de fazer. <br>
mas por conta deste cadáver aqui, <br>
não haverá nenhum ódio ou afeição como conseqüência."</p>
<br>

<p class=Normal style='margin-left:1.0in'>Diante dessas palavras arrepiantes e extraordinárias,
Os ladrões abaixaram suas espadas e disseram o seguinte:<br>

"Venerável senhor, o que você fez e quem é seu instrutor,<br>
quais ensinamentos que levam alguém a tal liberdade de todo o sofrimento?"</p>
<br>
<p class=Normal style='margin-left:1.0in'><b>Adhimutta:</b></p>

<p class=Normal style='margin-left:1.0in'>"Que tudo conhece e tudo vê, o Conquistador e Vitorioso, <br>
de compaixão infinita foi meu instrutor; <br>
ele é o mestre do mundo e também seu médico.</p>

<p class=Normal style='margin-left:1.0in'>"Ele me ensinou este Dhamma incomparável levando à cessação, <br>
que, quando cultivado conduz à libertação de toda tristeza."</p>

<p class=Normal style='margin-left:1.0in'>Agora, tendo ouvido estas palavras, bem ditas pelo sábio, <br>
com espadas abaixadas e as suas armas deixadas de lado, <br>
alguns ladrões desistiram de sua ocupação criminosa, <br>
enquanto que outros deixaram o mundo para viver a vida santa.</p>

<p class=Normal style='margin-left:1.0in'>Dedicados aos ensinamentos do Bem-aventurado, <br>
desenvolvendo os fatores da iluminação, <br>
os "poderes espirituais", a sabedoria, e a mente superior, <br>
com os corações alegres e com faculdades maduras, <br>
eles tocaram o estado incondicionado; Nibbana.</p>



