<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Certa
ocasião, o Venerável Sariputta e o Venerável Mahakotthita estavam em Benares,
no Parque do Gamo em Isipatana. Então, ao anoitecer, o Venerável Mahakotthita
saiu do seu isolamento e foi até o Venerável Sariputta e ambos se
cumprimentaram. Quando a conversa cortês e amigável havia terminado ele sentou
a um lado e disse:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Como é,
