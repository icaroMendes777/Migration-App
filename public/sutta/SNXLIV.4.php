<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigo, o Abençoado
não declarou isso: ‘O Tathagata existe após a morte.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
amigo Sariputta, o Tathagata não existe após a morte?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigo, o
Abençoado não declarou isso: ‘O Tathagata não existe após a morte.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
amigo Sariputta, o Tathagata tanto existe como não existe após a morte?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigo, o
Abençoado não declarou isso: ‘O Tathagata tanto existe como não existe após a
morte.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
amigo Sariputta, o Tathagata nem existe, nem não existe após a morte?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigo, o
Abençoado não declarou isso: ‘O Tathagata nem existe, nem não existe após a
morte.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Qual,
amigo, é a causa e razão porque isso não foi declarado pelo Abençoado?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigo, é
aquele que não compreende e não vê a forma como na verdade ela é, que não
compreende e não vê a sua origem, cessação e o caminho que conduz à sua
cessação, que pensa: ‘O Tathagata existe após a morte,’ ou ‘O Tathagata não
existe após a morte,’ ou ‘O Tathagata tanto existe como não existe após a
morte,’ ou ‘O Tathagata nem existe, nem não existe após a morte.’ É aquele que
não compreende e não vê a sensação como na verdade ela é .... que não
compreende e não vê a percepção como na verdade ela é ... que não compreende e
não vê as formações como na verdade elas são ... que não compreende e não vê a
consciência como na verdade ela é, que não compreende e não vê a sua origem,
cessação e o caminho que conduz à sua cessação, que pensa: ‘O Tathagata existe
após a morte,’ ... ou ‘O Tathagata nem existe, nem não existe após a morte.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas,
amigo, aquele que compreende e vê a forma ... sensação ... percepção ...
formações ... consciência como na verdade ela é, que compreende e vê a sua
origem, cessação e o caminho que conduz à sua cessação, não pensa: ‘O Tathagata
existe após a morte,’ ... ou ‘O Tathagata nem existe, nem não existe após a
morte.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Essa,
amigo, é a causa e razão porque isso não foi declarado pelo Abençoado.”<o:p></o:p></span></p>


