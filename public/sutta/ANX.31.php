<p class=Normal> Então o venerável Upali foi até o Abençoado e depois de cumprimentá-lo, sentou a um lado e disse: </p>

<p class=Normal>"Venerável senhor, quais foram as razões que o Tathagata estabeleceu as regras de treinamento para os seus discípulos e recitou o Patimokkha?" </p>

<p class=Normal>"Upali, foi com base em dez razões que o Tathagata estabeleceu as regras de treinamento para os seus discípulos e recitou o Patimokkha. Quais dez? (1) Para a excelência da Sangha; (2) para o bem-estar da Sangha; (3) para manter sob controle pessoas recalcitrantes; (4) para que bhikkhus bem comportados permaneçam descontraídos; (5) para a contenção das impurezas relativas a esta vida; (6) para a contenção das impurezas relativas a vidas futuras; (7) de modo que aqueles que não têm fé possam obter fé; (8) para incrementar a fé daqueles que têm fé; (9) para a continuidade do bom Dhamma; (10) para promover a disciplina. </p>

<p class=Normal>"Foi com base nessas razões que o Tathagata estabeleceu as regras de treinamento para os seus discípulos e recitou o Patimokkha."
</p>


