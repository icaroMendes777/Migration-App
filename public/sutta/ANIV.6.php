<p class=Normal>"Bhikkhus, há esses quatro tipos de pessoas que podem ser encontradas no mundo. Quais quatro? Aquela que possui pouco aprendizado e que não aplica o que aprendeu; aquela que possui pouco aprendizado e que aplica o que aprendeu; aquela que possui muito aprendizado e que não aplica o que aprendeu; aquela que possui muito aprendizado e que aplica o que aprendeu. </p>

<p class=Normal><a name=R1></a> (1) "E como é a pessoa que possui pouco aprendizado e que não aplica o que aprendeu? Aqui, a pessoa aprendeu pouco, isto é, sumários, prosa e verso, análises, versos, exclamações, símiles, histórias de vidas passadas, eventos maravilhosos, perguntas e respostas [<a href="#N1">1</a>], e ela não compreende o significado daquilo que aprendeu; ela não compreende o Dhamma, e ela não pratica de acordo com o Dhamma. Dessa forma, essa pessoa aprendeu pouco e não aplica o que aprendeu. </p>

<p class=Normal> (2) "E como é a pessoa que possui pouco aprendizado e que aplica o que aprendeu? Aqui, a pessoa aprendeu pouco, isto é, sumários, prosa e verso, análises, versos, exclamações, símiles, histórias de vidas passadas, eventos maravilhosos, perguntas e respostas, mas ela compreende o significado daquilo que aprendeu; e tendo  compreendido o Dhamma, ela pratica de acordo com o Dhamma. Dessa forma, essa pessoa aprendeu pouco e aplica o que aprendeu. </p>

<p class=Normal> (3) "E como é a pessoa que possui muito aprendizado e que não aplica o que aprendeu? Aqui, a pessoa aprendeu muito, isto é, sumários, prosa e verso, análises, versos, exclamações, símiles, histórias de vidas passadas, eventos maravilhosos, perguntas e respostas, mas ela não compreende o significado daquilo que aprendeu; ela não compreende o Dhamma, e ela não pratica de acordo com o Dhamma. Dessa forma, essa pessoa aprendeu muito e não aplica o que aprendeu. </p>

<p class=Normal> (4) "E como é a pessoa que possui muito aprendizado e que aplica o que aprendeu? Aqui, a pessoa aprendeu muito, isto é, sumários, prosa e verso, análises, versos, exclamações, símiles, histórias de vidas passadas, eventos maravilhosos, perguntas e respostas, e ela compreende o significado daquilo que aprendeu; e tendo  compreendido o Dhamma, ela pratica de acordo com o Dhamma. Dessa forma, essa pessoa aprendeu muito e aplica o que aprendeu. </p>

<p class=Normal>"Esses, bhikkhus, são os quatro tipos de pessoas que podem ser encontradas no mundo." </p>

<p class=Normal>Alguém com pouco aprendizado<br>
que não tenha a virtude estabelecida, <br>
é criticado por ambos, <br>
conduta virtuosa e aprendizado. </p>

<p class=Normal>Alguém com pouco aprendizado<br>
mas com a virtude bem estabelecida, <br>
é elogiado por sua conduta virtuosa; <br>
seu aprendizado foi bem sucedido. </p>

<p class=Normal>Alguém com muito aprendizado<br>
que não tenha a virtude estabelecida, <br>
é criticado por sua conduta não virtuosa; <br>
seu aprendizado não foi bem sucedido. </p>

<p class=Normal>Alguém com muito aprendizado<br>
com a virtude bem estabelecida<br>
é elogiado por ambos, <br>
conduta virtuosa e aprendizado. </p>

<p class=Normal>Quando um discípulo do Buda possui muito aprendizado, <br>
um experto no Dhamma, dotado com sabedoria, <br>
tal como uma moeda de ouro montanhês refinado, <br>
quem poderá criticá-lo? <br>
Até mesmo os devas o elogiam; <br>
por Brahma ele também é elogiado. </p>


