<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. [<a href="#NA">A</a>] <a name=RA></a>Em certa ocasião o Abençoado estava em Savathi, no Bosque de Jeta, no
Parque de Anathapindika. Lá ele se dirigiu aos monges desta forma: “Bhikkhus”.
– “Venerável Senhor”, eles responderam. O Abençoado disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.
“Bhikkhus, eu ensinarei para vocês a nobre concentração correta com as suas condições e os seus requisitos. [<a href="#N1">1</a>] <a name=R1></a>Ouçam e
prestem muita atenção ao que vou dizer.” – “Sim, venerável senhor,” os
bhikkhus responderam. O Abençoado disse o seguinte:<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. “O que, bhikkhus, é a
nobre concentração correta com as suas condições e os seus requisitos, isto é,
entendimento correto, pensamento correto, linguagem correta, ação correta, modo
de vida correto, esforço correto e atenção plena correta? A unificação da mente
equipada com esses sete fatores é chamada de nobre concentração correta com as suas condições e os seus requisitos.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(Entendimento)</b><o:p></o:p></span></p>

<p class=Normal><a name=T4></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. “Aqui,
bhikkhus, o entendimento correto vem primeiro. [<a href="#N2">2</a>] <a
name=R2></a>E como é que o entendimento correto vem primeiro? A pessoa compreende
o entendimento incorreto como entendimento incorreto e entendimento correto
como entendimento correto: esse é o entendimento correto de uma pessoa. [<a
href="#N3">3</a>] <o:p></o:p></span></p>

<p class=Normal><a name=R3></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. “E o que,
bhikkhus, é entendimento incorreto? 'Não existe nada que é dado, nada<span
style="mso-spacerun: yes">  </span>que é oferecido, nada que é sacrificado; não
existe fruto ou resultado de ações boas ou más; não existe este mundo nem outro
mundo; não existe mãe, nem pai; nenhum ser que renasça espontaneamente; não
existem no mundo brâmanes nem contemplativos bons e virtuosos que, após terem
conhecido e compreendido diretamente por eles mesmos, proclamam este mundo e o
próximo.' Isto é entendimento incorreto. <o:p></o:p></span></p>

<p class=Normal> 6. </span><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“E o que, bhikkhus, é entendimento correto? Entendimento correto, eu
digo, tem duas partes: existe o entendimento correto que está sujeito às
impurezas, que participa dos méritos, amadurece onde existe apego; [<a href="#N4">4</a>] </span><a name=R4></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>e existe o entendimento correto que é nobre,
imaculado, supramundano, um elemento do caminho. <o:p></o:p></span></p>

<p class=Normal> 7. </span><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“E o que, bhikkhus, é o entendimento correto que está sujeito às
impurezas, que participa dos méritos, amadurece onde existe apego? ‘Existe
aquilo que é dado e o que é oferecido e o que é sacrificado; existe fruto e
resultado de boas e más ações; existe este mundo e o outro mundo; existe a mãe
e o pai; existem seres que renascem espontaneamente; existem no mundo brâmanes
e contemplativos bons e virtuosos que, após terem conhecido e compreendido
diretamente por eles mesmos, proclamam este mundo e o próximo.’ Esse é o
entendimento correto que está sujeito às impurezas, que participa dos méritos,
amadurece onde existe apego. <o:p></o:p></span></p>

<p class=Normal> 8. “E o que, bhikkhus, é o entendimento
correto que é nobre, imaculado, supramundano, um elemento do caminho? A sabedoria, a
faculdade da sabedoria, o poder da sabedoria, o fator da iluminação da
investigação dos fenômenos, o entendimento correto como elemento do caminho na
mente daquele que é nobre, cuja mente é imaculada, que possui o nobre caminho e
está desenvolvendo o nobre caminho: [<a href="#N5">5</a>]
</span><a name=R5></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>esse é o
entendimento correto que é nobre, imaculado, supramundano, um elemento do
caminho. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. &quot;A pessoa faz o
esforço para abandonar o entendimento incorreto e penetrar o entendimento
correto: esse é o esforço correto da pessoa. A pessoa com atenção plena
abandona o entendimento incorreto e penetra e permanece com o entendimento
correto: essa é a atenção plena de uma pessoa. Assim essas três qualidades -
entendimento correto, esforço correto e atenção plena correta – giram em torno
do entendimento correto.&quot; [<a href="#N6">6</a>]</span><span
lang=PT-BR style='mso-ansi-language:PT-BR'><o:p></o:p></span></p>

<p class=Normal><a name=R6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'><b>(Pensamento)</b><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10.
“Aqui, bhikkhus, o entendimento correto vem primeiro. E como é que o
entendimento correto vem primeiro? A pessoa compreende o pensamento incorreto
como pensamento incorreto e pensamento correto como pensamento correto: esse é
o entendimento correto de uma pessoa. [<a href="#N7">7</a>] <o:p></o:p></span></p>

<p class=Normal><a name=R7></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>11. E o que, bhikkhus, é pensamento incorreto? O pensamento da
sensualidade, o pensamento da má vontade, o pensamento da crueldade: isso é
pensamento incorreto.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. “E o que, bhikkhus, é
pensamento correto? Pensamento correto, eu digo, tem duas partes: existe o
pensamento correto que está sujeito às impurezas, que participa dos méritos,
amadurece onde existe apego; e existe o pensamento correto que é nobre,
imaculado, supramundano, um elemento do caminho. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. “E o que, bhikkhus, é o
pensamento correto que está sujeito às impurezas, que participa dos méritos,
amadurece onde existe apego? O pensamento da renúncia, o pensamento da não má
vontade e o pensamento da não crueldade: [<a href="#N8">8</a>] <a name=R8></a>esse
é o pensamento correto que está sujeito às impurezas ... amadurece onde existe
apego.  </span><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14. “E o que, bhikkhus, é o
pensamento correto que é nobre, imaculado, supramundano, um elemento do
caminho? O pensamento, idéia, intenção, absorção mental, fixação mental,
direção da mente, formação verbal naquele cuja mente é nobre, cuja mente
é imaculada, que possui o nobre caminho e está desenvolvendo o nobre caminho: [<a href="#N9">9</a>]<a name=R9></a> esse é o pensamento correto que é nobre ... um
elemento do caminho.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. &quot;A pessoa faz o
esforço para abandonar o pensamento incorreto e penetrar no pensamento correto:
esse é o esforço correto da pessoa. A pessoa com atenção plena abandona o
pensamento incorreto e penetra e permanece no pensamento correto: essa é a
atenção plena correta da pessoa. Dessa forma essas três qualidades -
entendimento correto, esforço correto e atenção plena correta – giram em torno
do pensamento correto.&quot; [<a href="#N10">10</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R10></a><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(Linguagem)</b><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16. “Aqui, bhikkhus, o
entendimento correto vem primeiro. E como é que o entendimento correto vem
primeiro?<span style="mso-spacerun: yes">  </span>A pessoa compreende a
linguagem incorreta como linguagem incorreta e linguagem correta como linguagem
correta: esse é o entendimento correto. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17. “E o que, bhikkhus, é
linguagem incorreta? Linguagem mentirosa, linguagem maliciosa, linguagem
grosseira e linguagem frívola: isto é linguagem incorreta. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18. “E o que, bhikkhus, é
linguagem correta? Linguagem correta, eu digo, tem duas partes: existe a
linguagem correta que está sujeita às impurezas, que participa dos méritos,
amadurece onde existe apego; e existe a linguagem correta que é nobre,
imaculada, supramundana, um elemento do caminho. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>19. “E o que, bhikkhus, é a
linguagem correta que está sujeita às impurezas, que participa dos méritos,
amadurece onde existe apego? Abstinência de linguagem mentirosa, abstinência de
linguagem maliciosa, abstinência de linguagem grosseira, abstinência de
linguagem frívola: essa é a linguagem correta que está sujeita às
impurezas ... amadurece onde existe apego. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>20. “E o que, bhikkhus, é a
linguagem correta que é nobre, imaculada, supramundana, um elemento do caminho?
A desistência dos quatro tipos de uso impróprio da linguagem, a privação, a
contenção, a abstinência deles naquele cuja mente é nobre, cuja mente é
imaculada, que possui o nobre caminho e está desenvolvendo o nobre caminho: [<a
href="#N11">11</a>] <a name=R11></a>essa é a linguagem correta que é nobre ... um
elemento do caminho.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>21. &quot;A pessoa faz o
esforço para abandonar a linguagem incorreta e penetrar a linguagem correta:
esse é o esforço correto da pessoa. A pessoa com atenção plena abandona a
linguagem incorreta e penetra e permanece com a linguagem correta: essa é a
atenção plena correta da pessoa. Assim essas três qualidades - entendimento
correto, esforço correto e atenção plena correta – giram em torno da linguagem
correta.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(Ação)</b><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>22. “Aqui, bhikkhus, o
entendimento correto vem primeiro. E como é que o entendimento correto vem
primeiro?<span style="mso-spacerun: yes">  </span>A pessoa compreende a ação
incorreta como ação incorreta e ação correta como ação correta: esse é o
entendimento correto.“ <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>23. “E o que, bhikkhus, é
ação incorreta? Matar seres vivos, tomar aquilo que não é dado e comportamento
impróprio em relação aos prazeres sensuais: essa é a ação incorreta.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>24. “E o que, bhikkhus, é  ação correta?
Ação correta, eu digo, tem duas partes: existe a ação correta que está sujeita
às impurezas, que participa dos méritos, amadurece onde existe apego; e existe
a ação correta que é nobre, imaculada, supramundana, um elemento do caminho.<o:p></o:p></span></p>

<p class=Normal> 25. </span><span lang=PT-BR style='mso-ansi-language:
PT-BR'>E o que, bhikkhus, é a ação correta que está sujeita às impurezas, que
participa dos méritos, amadurece onde existe apego? Abster-se de matar seres
vivos, abster-se de tomar aquilo que não é dado, abster-se de comportamento
impróprio em relação aos prazeres sensuais: essa é a ação correta que está
sujeita às impurezas ... amadurece onde existe apego.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>26. “E o que, bhikkhus, é a
ação correta que é nobre, imaculada, supramundana, um elemento do caminho? A desistência
dos três tipos de comportamento impróprio com o corpo, a privação, a contenção, a abstinência deles naquele cuja mente é nobre, cuja mente é imaculada, que
possui o nobre caminho e está desenvolvendo o nobre caminho:<span
style="mso-spacerun: yes">  </span>essa é a ação correta que é nobre ... um
elemento do caminho.<o:p></o:p></span></p>

<p class=Normal> 27. </span><span lang=PT-BR style='mso-ansi-language:
PT-BR'>&quot;A pessoa faz o esforço para abandonar a ação incorreta e penetrar na
ação correta: esse é o esforço correto da pessoa. A pessoa com atenção plena abandona
a ação incorreta e entra e permanece na ação correta: essa é a atenção plena
correta da pessoa. Assim essas três qualidades - entendimento correto, esforço
correto e atenção plena correta – giram em torno da ação correta.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(Modo de Vida)</b><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>28. “Aqui, bhikkhus, o
entendimento correto vem primeiro. E como é que o entendimento correto vem
primeiro?<span style="mso-spacerun: yes">  </span>A pessoa compreende modo de
vida incorreto como modo de vida incorreto e modo de vida correto como modo de
vida correto: esse é o entendimento correto.“ <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>29. “E o que, bhikkhus, é
modo de vida incorreto? Maquinar, lisonjear, insinuar, desdenhar e perseguir o
ganho com o ganho: isso é modo de vida incorreto. [<a href="#N12">12</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R12></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>30. “E o
que, bhikkhus, é modo de vida correto? Modo de vida correto, eu digo, tem duas
partes: existe o modo de vida que está sujeito às impurezas, que participa dos
méritos, amadurece onde existe apego; e existe o modo de vida correto que é
nobre, imaculado, supramundano, um elemento do caminho</span><span lang=PT-BR
style='font-size:11.0pt;mso-bidi-font-size:12.0pt;mso-ansi-language:PT-BR'>.<o:p></o:p></span></p>

<p class=Normal> 31. </span><span lang=PT-BR style='mso-ansi-language:
PT-BR'>E o que, bhikkhus, é o modo de vida correto que está sujeito às
impurezas, que participa dos méritos, amadurece onde existe apego? Aqui,
bhikkhus, um nobre discípulo abandona o modo de vida incorreto e ganha a vida
através do modo de vida correto: esse é o modo de vida correto que está sujeito
às impurezas ... amadurece onde existe apego.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>32. “E o que, bhikkhus, é
o modo de vida correto que é nobre, imaculado, supramundano, um elemento do
caminho? A desistência do modo de vida incorreto, a privação, a contenção, a
abstinência naquele cuja mente é nobre, cuja mente é imaculada, que possui o
nobre caminho e está desenvolvendo o nobre caminho: esse é o modo de vida
correto que é nobre ... um elemento do caminho.<o:p></o:p></span></p>

<p class=Normal> 33. </span><span lang=PT-BR style='mso-ansi-language:
PT-BR'>&quot;A pessoa faz esforço para abandonar o modo de vida incorreto e
penetrar no modo de vida correto: esse é o esforço correto da pessoa. A pessoa
com atenção plena abandona o modo de vida incorreto e entra e permanece no modo
de vida correto: essa é a atenção plena correta da pessoa. Assim essas três
qualidades - entendimento correto, esforço correto e atenção plena correta –
giram em torno do modo de vida correto.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(Os Quarenta Notáveis)</b><o:p></o:p></span></p>

<p class=Normal><a name=T34></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>34. “Aqui,
bhikkhus, o entendimento correto vem primeiro. E como o entendimento correto
vem primeiro? Naquele que possui entendimento correto, o pensamento correto
surge; [<a href="#N13">13</a>] <a name=R13></a>naquele que possui pensamento
correto, a linguagem correta surge; naquele que possui linguagem correta, a
ação correta surge; naquele que possui a ação correta, o modo de vida correto surge;
naquele que possui modo de vida correto, o esforço correto surge; naquele que
possui o esforço correto, a atenção plena correta surge; naquele que possui
atenção plena correta, a concentração correta surge; naquele que possui a
concentração correta, o conhecimento correto surge; naquele que possui
conhecimento correto, a libertação correta surge. Dessa forma bhikkhus, o
caminho do discípulo que se encontra no treinamento superior possui oito
elementos, o arahant possui dez elementos. [<a href="#N14">14</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R14></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>35. “Aqui,
bhikkhus, o entendimento correto vem primeiro. E como o entendimento correto
vem primeiro? Naquele que possui entendimento correto, o entendimento incorreto
é abolido e os vários estados ruins e prejudiciais que se originam do
entendimento incorreto como condição, também são abandonados e os vários
estados benéficos que se originam do entendimento correto como condição, são
realizados através do desenvolvimento.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Naquele que possui
pensamento correto, o pensamento incorreto é abandonado e os vários estados
ruins e prejudiciais que se originam do pensamento incorreto como condição,
também são abandonados e os vários estados benéficos que se originam do
pensamento correto como condição, são realizados através do desenvolvimento.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Naquele que possui
linguagem correta, a linguagem incorreta é abandonada ... Naquele que possui
ação correta, a ação incorreta é abandonada ... Naquele que possui modo de vida
correto, o modo de vida incorreto é abandonado ... Naquele que possui esforço correto,
o esforço incorreto é abandonado ... Naquele que possui atenção plena correta,
a atenção plena incorreta é abandonada ... Naquele que possui concentração
correta, a concentração incorreta é abandonada ... Naquele que possui
conhecimento correto, o conhecimento incorreto é abandonado ... Naquele que
possui libertação correta, a libertação incorreta é abandonada e os vários
estados ruins e prejudiciais, que se originam da libertação incorreta como
condição, também são abandonados e os vários estados benéficos que se originam
da libertação correta como condição, são realizados através do desenvolvimento.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>36. “Dessa forma, bhikkhus,
há vinte fatores benéficos e vinte fatores prejudiciais. [<a href="#N15">15</a>]
<a name=R15></a>Este discurso do Dhamma acerca dos Quarenta Notáveis foi
colocado em movimento e não poderá ser detido por nenhum contemplativo ou
brâmane, divindade ou deus ou Mara ou Brahma ou qualquer um no mundo.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>37. “Bhikkhus, se algum
contemplativo ou brâmane pensar que este discurso acerca dos Quarenta Notáveis
deva ser censurado ou rejeitado, então existem dez deduções legítimas das suas
afirmações que proporcionam razões para censurá-lo no aqui e agora. Se essa
pessoa censurar o entendimento correto, então ela honra e louva<span
style="mso-spacerun: yes">  </span>aqueles contemplativos e brâmanes que
possuem entendimento incorreto. Se essa pessoa censurar o pensamento correto,
então ela honra e louva<span style="mso-spacerun: yes">  </span>aqueles
contemplativos e brâmanes que possuem pensamento incorreto. Se essa pessoa censurar
a linguagem correta ... a ação correta ... o modo de vida correto ... o esforço
correto ... a atenção plena correta ... a concentração correta ... o
conhecimento correto ... a libertação correta, então ela honra e louva<span
style="mso-spacerun: yes">  </span>aqueles contemplativos e brâmanes que
possuem a libertação incorreta. Se algum contemplativo ou brâmane pensar que
este discurso acerca dos Quarenta Notáveis deva ser censurado ou rejeitado,
então estas são dez deduções legítimas das suas afirmações que proporcionam
razões para censurá-lo no aqui e agora.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>38. “Bhikkhus, mesmo os
mestres de Okkala, Vassa e Bhañña, [<a href="#N16">16</a>] <a name=R16></a>que
acreditam na doutrina da não causalidade, a doutrina da não ação e a doutrina
do niilismo, não pensariam que este discurso do Dhamma acerca dos Quarenta
Notáveis<span style="mso-spacerun: yes">  </span>deva ser censurado ou
rejeitado. Por que isso? Pelo medo da crítica, ataque e confrontação.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o que disse o
Abençoado. Os bhikkhus ficaram satisfeitos e contentes com as palavras do
Abençoado.<o:p></o:p></span></p>


