<p class=Normal><a name=NA></a>[A] No estudo <a href="https://www.buddhismuskunde.uni-hamburg.de/pdf/5-personen/analayo/mahacattarisaka.pdf"><i>The Mahacattarisaka-sutta in the Light of its Parallels - Tracing the Beginnings of Abhidharmic Thought</i></a> o Bhikkhu Analayo argumenta que todas as referências neste sutta ao caminho supramundano seriam adições tardias que não traçam a sua origem ao Buda mas sim ao desenvolvimento do Abhidhamma. Essas referências não são encontradas nas versões paralelas preservadas em Chinês e Tibetano. [<a href="#RA">Retorna</a>]</p>

<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] <i>Ariyam samma samadhim sa-upanisam saparikkharam. </i>MA explica
que neste caso “nobre” quer dizer supramundano, e explica que esta é a concentração que faz
parte do caminho supramundano. As suas “condições e requisitos”, como será
mostrado, são os outros sete elementos do caminho. [<a href="#R1">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[2] <i>Pubbangama,
</i>literalmente “o precursor”. MA<span style="mso-spacerun: yes">  </span>diz
que dois tipos de entendimento correto são precursores: o entendimento correto
do insight, que investiga as formações como impermanentes, insatisfatórias e
não-eu; e o entendimento correto do caminho supramundano que surge como uma
conseqüência do insight e realiza a radical destruição das impurezas. O
entendimento correto do insight como precursor parece estar sendo mostrado nos
versos 4, 10, 16, 22 e 28; o entendimento correto do caminho supramundano como
precursor nos versos 34 e 35. [<a href="#R2">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N3></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[3] Esta
frase sugere que, para adquirir o entendimento correto acerca da natureza da
realidade, é necessário primeiro ser capaz de distinguir entre os ensinamentos
corretos e incorretos sobre a natureza da realidade. MA diz que esse é o
entendimento correto do insight que percebe o entendimento incorreto como um
objeto, através da penetração das suas características de impermanência, etc e
que percebe o entendimento correto através do exercício da função de
compreensão e assim esclarece a confusão. [<a href="#R3">Retorna</a>]<o:p></o:p></span></p>

<p class=Normal><a name=N4></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[4] Esse é
o entendimento correto mundano, um elemento que traz mérito e que conduz a um renascimento
favorável, mas que não é capaz de por si só realizar a transcendência da
existência condicionada.<span style="mso-spacerun: yes">  </span>[<a href="#R4">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N5></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[5] Esta
definição explica que o entendimento correto supramundano é a sabedoria <i>(pañña)
</i>que é encontrada entre os 37 apoios para a iluminação como: uma faculdade,
um poder, fator de iluminação e elemento do caminho. A definição é formulada
através da função cognitiva ao invés do conteúdo objetivo do entendimento. Em
um outro sutta (<a href="MN141.php#T24">MN 141.24</a>) o entendimento correto
do caminho é definido como a compreensão das Quatro Nobres Verdades. Podemos
entender que o entendimento conceitual das quatro verdades se encaixa no
entendimento correto mundano, enquanto que a penetração das verdades com o conhecimento direto,
através da realização de Nibbana, constitui o entendimento correto
supramundano. [<a href="#R5">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N6></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[6] MA:
Eles acompanham o entendimento correto como coexistentes e precursores. Esforço
correto e atenção plena correta coexistem com o entendimento correto
supramundano; o entendimento correto do insight é o precursor do entendimento
correto supramundano. [<a href="#R6">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N7></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[7] MA explica isto como sendo o entendimento correto do insight que
entende o pensamento correto por meio da sua função e assim esclarece a confusão.
Parece, no entanto, que a questão aqui é uma diferenciação mais elementar dos
dois tipos de pensamento. [<a href="#R7">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N8></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[8] Esta é a definição padrão de pensamento correto como um elemento do
Nobre Caminho óctuplo; veja o <a href="MN141.php#T25">MN 141.25</a>. [<a
href="#R8">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N9></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[9] Nesta definição, o elemento do pensamento, <i>(sankappa)</i>,<i> </i>é
identificado com o pensamento aplicado, <i>(vitakka)</i>,<i> </i>que mais adiante
é especificado como o fator responsável pela absorção através da fixação e
direção da mente sobre o seu objeto. O pensamento aplicado como “formação
verbal” no <a href="MN44.php#T15">MN 44.15</a>. [<a href="#R9">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N10></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[10] MA: Esta frase se refere exclusivamente aos fatores coexistentes
que acompanham o pensamento correto supramundano. Na fase preliminar da
prática, os três pensamentos corretos mundanos surgem separadamente, mas no
momento do caminho supramundano, um único pensamento correto surge eliminando o
tríplice pensamento incorreto. Dessa forma o pensamento correto supramundano
também pode ser descrito como o pensamento da renúncia, não má vontade e não
crueldade. O mesmo método se aplica à linguagem correta, etc. [<a href="#R10">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N11></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[11] Enquanto que a linguagem correta mundana é exercida de quatro modos
diferentes de acordo com o tipo de linguagem incorreta da qual há abstinência,
na ocasião do caminho supramundano, o único fator de linguagem correta exerce a
função quádrupla de eliminar as tendências em relação aos quatro tipos de
linguagem incorreta. O mesmo princípio se aplica à ação correta. [<a href="#R11">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N12></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[12] Essas são formas incorretas dos bhikkhus satisfazerem as suas necessidades.
MA diz que aquelas mencionadas no sutta não são as únicas formas de modo de
vida incorreto, que envolvem qualquer modo de satisfazer as suas necessidades que implique uma
transgressão dos preceitos. No <a href="ANV.177.php">AN V.177</a> o Buda menciona cinco tipos de
