<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
possuindo três qualidades, um bhikkhu vive pleno de alegria e felicidade nesta mesma
vida e ele terá assentado a fundação para a destruição das impurezas. Quais são
as três? Ele é aquele que guarda as portas dos meios dos sentidos, que é
moderado na alimentação e que é dedicado à vigilância.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como,
bhikkhus, um bhikkhu é aquele que guarda as portas dos meios dos sentidos?
Neste caso, ao ver uma forma com o olho, </span>ele não se agarra aos seus
sinais ou detalhes. Visto que, se permanecer com a faculdade do olho
descuidada, ele será tomado pelos estados ruins e prejudiciais de cobiça e
tristeza. Ele pratica a contenção, ele protege a faculdade do olho, ele se
empenha na contenção da faculdade do olho. Ao ouvir um som com o ouvido ... Ao
cheirar um aroma com o nariz ... Ao saborear um sabor com a língua ... Ao tocar um
tangível com o corpo ... Ao conscientizar um objeto mental<span
style="mso-spacerun: yes">  </span>com a<span style="mso-spacerun: yes"> 
</span>mente, ele não se agarra aos seus sinais ou detalhes. Visto que, se
permanecer com a faculdade da mente descuidada, ele será tomado pelos estados
ruins e prejudiciais de cobiça e tristeza. Ele pratica a contenção, ele protege
a faculdade da mente, ele se empenha na contenção da faculdade da mente<span
lang=PT-BR style='mso-ansi-language:PT-BR'>.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Suponham,
bhikkhus, que houvesse uma carruagem num terreno plano numa encruzilhada, com puros-sangues
arreados e a aguilhada preparada, de forma que um adestrador habilidoso, um
cocheiro domador de cavalos pudesse montar nela e, tomando as rédeas na mão
esquerda e a aguilhada na mão direita, pudesse ir e regressar por qualquer
caminho quando ele quisesse. Assim também, um bhikkhu treina para proteger
essas seis faculdades dos sentidos, treina para controlá-las, treina para
domesticá-las, treina para pacificá-las. É desse modo, bhikkhus, que um bhikkhu
guarda as portas dos meios dos sentidos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como,
bhikkhus, um bhikkhu é moderado na alimentação? Neste caso, refletindo de maneira
sábia, um bhikkhu não toma o alimento como forma de diversão ou para
embriaguez, tampouco com o objetivo de embelezamento e para ser mais atraente;
mas somente com o propósito de manter a resistência e continuidade deste corpo,
como forma de dar um fim ao desconforto e para auxiliar a vida santa,
considerando: ‘Dessa forma darei um fim às antigas sensações (de fome) sem
despertar novas sensações (de comida em excesso) e serei saudável e sem culpa e
viverei em comodidade.’ É dessa forma, bhikkhus, que ele é moderado na alimentação.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como,
bhikkhus, um bhikkhu é<span style="mso-spacerun: yes">  </span>dedicado à
vigilância? Neste caso, durante o dia, enquanto caminha para lá e para cá e
enquanto sentado, um bhikkhu purifica a mente dele dos estados obstrutivos. Na
primeira vigília da noite, enquanto estiver caminhando para lá e para cá e
enquanto sentado, ele purifica a mente dele dos estados obstrutivos. Na segunda
vigília da noite ele deita para dormir, no lado direito, na postura do leão com
um pé sobre o outro, atento e plenamente consciente, após anotar na mente o
horário para levantar. Após levantar, na terceira vigília da noite, enquanto
estiver caminhando para cá e para lá e enquanto sentado, ele purifica a mente
dele dos estados obstrutivos.’ É dessa forma, bhikkhus, que ele é dedicado à
vigilância.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
é possuindo essas três qualidades que um bhikkhu vive pleno de alegria e
felicidade nesta mesma vida e terá assentado a fundação para a destruição das
impurezas.”<o:p></o:p></span></p>


