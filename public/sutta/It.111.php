<p class=Normal> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: "Bhikkhus, vocês devem permanecer com a virtude perfeita, contidos pelas regras do Patimokkha, perfeitos na conduta e na sua esfera de atividades, temendo a menor falha, treinem adotando os preceitos de virtude. Permanecendo com a virtude perfeita, bhikkhus ... treinando adotando os preceitos de virtude, o que mais há para ser feito?
</p>

<p class=Normal> "Se enquanto estiver caminhando, em pé, sentado, ou deitado, um bhkkhu estiver livre do desejo sensual e da má vontade, livre da inquietação e da ansiedade, livre da preguiça e do torpor, e tiver abandonado a dúvida, a sua energia se tornará forte e incansável, a sua atenção plena estará alerta e clara, o seu corpo ficará calmo e relaxado, a mente concentrada e unificada. Um bhikkhu que dessa forma está ardente e teme cometer transgressões, é chamado de constantemente energético e decidido."</p>

<p class=Normal style='margin-left:.75in'> Controlado ao caminhar, <br>
controlado ao estar em pé, <br>
controlado quando sentado<br>
controlado quando deitado, <br>
controlado ao dobrar<br>
e esticar os membros -<br>
acima, de lado a lado, abaixo, <br>
até onde se estende o mundo, <br>
um bhikkhu observa como as coisas ocorrem, <br>
o surgimento e cessação dos agregados.</p>

<p class=Normal style='margin-left:.75in'> Permanecendo assim ardente, <br>
com a conduta calma e tranquila, <br>
sempre com atenção plena, ele treina<br>
tranquilizando a mente. <br>
Desse bhikkhu é dito<br>
aquele que sempre está decidido. </p>



