<p class=Normal>Assim ouvi. Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no Parque de Anathapindika.</p>



<p class=Normal>Agora, naquela ocasião, a noite era bastante escura e o Abençoado se encontrava sentado ao céu aberto enquanto lamparinas queimavam na dependência do óleo. E também naquela ocasião um grande número de insetos alados, mariposas, voavam ao redor e caíam nessas lamparinas, encontrando assim desgraça, ruína, ambas desgraça e ruína. E o Buda viu um grande número de insetos alados, mariposas, voavam ao redor e caíam nessas lamparinas, encontrando assim desgraça, ruína, ambas desgraça e ruína.</p>



<p class=Normal>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p>



	<p class=Normal style='margin-left:.75in'>"Aqueles empenhados pelo que é visto,<br>

	ou pelo que é ouvido,<br>

	precipitam-se de cabeça,<br> 

	omitindo o que é essencial.<br>

	Um a um, eles fazem com que novos elos surjam,<br>

	um a um, eles caem como insetos	em direção à chama.”</p>

