<h2><a name=conteudo></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Conteúdo dos Samyuttas:</span></h2>

<p class=Normal><a name=XXII></a><b style='mso-bidi-font-weight:normal'><span lang=PT-BR
style='mso-ansi-language:PT-BR'>XXII. Khandha-samyutta</span></b><span
lang=PT-BR style='mso-ansi-language:PT-BR'> <b>– Agregados</b></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O tópico
deste samyutta são os cinco agregados que foram o principal esquema de
categorias empregado pelo Buda para analisar os seres sencientes. O exame dos
cinco agregados desempenha um papel crítico nos ensinamentos do Buda por pelo
menos quatro razões. Primeiro, porque os cinco agregados são a referência
última para a primeira nobre verdade, a verdade do sofrimento, e visto que
todas as quatro nobres verdades revolvem em torno do sofrimento, a compreensão
dos agregados é essencial para a compreensão das quatro nobres verdades como um
todo. Segundo, porque os cinco agregados são o domínio objetivo do apego e
dessa forma contribuem como condição para o surgimento do sofrimento no futuro.
Terceiro, porque a remoção do apego é necessária para alcançar a libertação e o
apego tem que ser removido daqueles objetos nos quais os seus tentáculos estão
enrolados, isto é, os cinco agregados. Quarto, porque a remoção do apego é
obtida através da sabedoria e o tipo de sabedoria necessária é exatamente o
insight relacionado à real natureza dos agregados. </span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A análise
dos agregados empregada pelo Buda não é empreendida com o objetivo de alcançar
um entendimento objetivo, científico, do que é um ser humano da maneira como é
empreendido pela fisiologia e psicologia. Para o Buda, a investigação da
natureza da existência está sempre subordinada ao poder liberativo do Dhamma e
por essa razão só aqueles aspectos da existência que contribuem para a
realização desse objetivo recebem atenção.[</span><span style='font-family:
'><a href="#RXXII"><span lang=PT-BR style='mso-ansi-language:PT-BR'>Retorna</span></a></span><span
lang=PT-BR style='mso-ansi-language:PT-BR'>]</span></p>

