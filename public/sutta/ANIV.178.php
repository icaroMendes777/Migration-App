<p class=Normal> “Bhikkhus, há esses quatro tipos de pessoas que podem ser encontradas no mundo. Quais quatro?</p>

<p class=Normal> “Há o caso em que um bhikkhu entra e permanece numa certa libertação pacífica da mente. [<a href="#N1">1</a>] <a name="R1"> </a>
Ele se ocupa com a cessação da identificação de um eu, mas ocupando-se com a cessação da identificação de um eu a sua mente não salta, não ganha confiança, decisão e firmeza na cessação da identificação de um eu. Nele a cessação da identificação de um eu não pode ser esperada. Tal como se um homem agarrase um galho com a mão besuntada com resina, a mão dele ficaria grudada no galho, presa, aderida; da mesma forma, um bhikkhu entra e permanece numa certa libertação pacífica da mente. Ele se ocupa com a cessação da identificação de um eu, mas ocupando-se com a cessação da identificação de um eu a sua mente não salta, não ganha confiança, decisão e firmeza na cessação da identificação de um eu. Nele a cessação da identificação de um eu não pode ser esperada.
</p> 

<p class=Normal> “Agora, há o caso em que um bhikkhu entra e permanece numa certa libertação pacífica da mente. Ele se ocupa com a cessação da identificação de um eu, e ocupando-se com a cessação da identificação de um eu a sua mente salta, ganha confiança, decisão e firmeza na cessação da identificação de um eu. Nele a cessação da identificação de um eu pode ser esperada. Tal como se um homem agarrase um galho com a mão limpa, a mão dele não ficaria grudada no galho, presa, aderida; da mesma forma, um  bhikkhu entra e permanece numa certa libertação pacífica da mente. Ele se ocupa com a cessação da identificação de um eu, e ocupando-se com a cessação da identificação de um eu a sua mente salta, ganha confiança, decisão e firmeza na cessação da identificação de um eu. Nele a cessação da identificação de um eu pode ser esperada. </p>

<p class=Normal> “Agora, há o caso em que um bhikkhu entra e permanece numa certa libertação pacífica da mente. Ele se ocupa com o rompimento da ignorância, mas ocupando-se com o rompimento da ignorância a sua mente não salta, não ganha confiança, decisão e firmeza no rompimento da ignorância. Nele o rompimento da ignorância não pode ser esperado. Tal como se houvesse uma lagoa de efluentes, existindo há muito tempo, e um homem bloqueasse todos os drenos de entrada e abrisse todos os drenos de saída e do céu não caíssem gotas pesadas de chuva: o rompimento da lagoa de efluentes não poderia ser esperado; da mesma forma, um bhikkhu entra e permanece numa certa libertação pacífica da mente. Ele se ocupa com o rompimento da ignorância, mas ocupando-se com o rompimento da ignorância a sua mente não salta, não ganha confiança, decisão e firmeza no rompimento da ignorância. Nele o rompimento da ignorância não pode ser esperado. </p>

<p class=Normal> “Agora, há o caso em que um bhikkhu entra e permanece numa certa libertação pacífica da mente. Ele se ocupa com o rompimento da ignorância, e ocupando-se com o rompimento da ignorância a sua mente salta, ganha confiança, decisão e firmeza no rompimento da ignorância. Nele o rompimento da ignorância pode ser esperado. Tal como se houvesse uma lagoa de efluentes, existindo há muito tempo, e um homem abrisse todos os drenos de entrada e bloqueasse todos os drenos de saída e do céu caíssem gotas pesadas de chuva: o rompimento da lagoa de efluentes poderia ser esperado; da mesma forma, um bhikkhu entra e permanece numa certa libertação pacífica da mente. Ele se ocupa com o rompimento da ignorância, e ocupando-se com o rompimento da ignorância a sua mente salta, ganha confiança, decisão e firmeza no rompimento da ignorância. Nele o rompimento da ignorância pode ser esperado. </p>

<p class=Normal> “Esses são os quatro tipos de pessoas que podem ser encontradas no mundo.”</p>


