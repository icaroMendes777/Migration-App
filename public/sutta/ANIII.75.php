<p class=Normal> Então o venerável Ananda foi até o Abençoado e depois de cumprimentá-lo sentou a um lado. O Abençoado disse:

<p class=Normal>"Ananda, aqueles pelos quais você tenha compaixão e que pensem que você deva ser ouvido, quer sejam amigos ou companheiros, parentes ou familiares, devem ser encorajados, estimulados e estabelecidos por você em três coisas. Quais três? </p>

<p class=Normal> (1) "Eles devem ser encorajados, estimulados e estabelecidos com perfeita claridade, serenidade e confiança no Buda assim: 'De fato, o Abençoado é um arahant, perfeitamente iluminado, consumado no verdadeiro conhecimento e conduta, bem-aventurado, conhecedor dos mundos, um líder insuperável de pessoas preparadas para serem treinadas, mestre de devas e humanos, desperto, sublime.'"</p>

<p class=Normal> (2) "Eles devem ser encorajados, estimulados e estabelecidos com perfeita claridade, serenidade e confiança no Dhamma assim: 'O Dhamma é bem proclamado pelo Abençoado, visível no aqui e agora, com efeito imediato, que convida ao exame, que conduz para adiante, para ser experimentado pelos sábios por eles mesmos.'"</p>

<p class=Normal> (3) "Eles devem ser encorajados, estimulados e estabelecidos com perfeita claridade, serenidade e confiança na Sangha assim: 'A Sangha dos discípulos do Abençoado pratica o bom caminho, pratica o caminho reto, pratica o caminho verdadeiro, pratica o caminho adequado, isto é, os quatro pares de pessoas, os oito tipos de indivíduos; esta Sangha dos discípulos do Abençoado é merecedora de dádivas, merecedora de hospitalidade, merecedora de oferendas, merecedora de saudações com reverência, um campo inigualável de mérito para o mundo.'"</p>

<p class=Normal>"Podem ocorrer, Ananda, alterações nos quatro grandes elementos - no elemento terra, no elemento água, no elemento fogo, no elemento ar - mas não é possível ocorrer alterações no nobre discípulo que tem perfeita claridade, serenidade e confiança no Buda. Neste contexto, isto seria uma alteração: que esse nobre discípulo, que possui perfeita claridade, serenidade e confiança no Buda, possa renascer no inferno, no mundo animal, no mundo dos fantasmas famintos. Isso é impossível. </p>

<p class=Normal>"Podem ocorrer, Ananda, alterações nos quatro grandes elementos - no elemento terra, no elemento água, no elemento fogo, no elemento ar - mas não é possível ocorrer alterações no nobre discípulo que tem perfeita claridade, serenidade e confiança no Dhamma ... na Sangha. Neste contexto, isto seria uma alteração: que esse nobre discípulo, que possui perfeita claridade, serenidade e confiança no Dhamma, possa renascer no inferno, no mundo animal, no mundo dos fantasmas famintos. Isso é impossível. </p>

