<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’Venham, amigos,
permaneçam com as suas faculdades dos sentidos protegidas, com a atenção plena
como seu protetor, com a atenção plena como o chefe, com a mente protegida por
vocês mesmos, com a mente protegida pela atenção plena.’ Assim eles devem ser
encorajados, exortados e estabelecidos na contenção dos sentidos. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’Venham, amigos, falem
pouco, estabeleçam limites nas suas conversações.’ Assim eles devem ser
encorajados, exortados e estabelecidos em limitar as conversações. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’Venham, amigos, permaneçam
n<a name=wild>as florest</a>as. Recorram a moradias remotas nas florestas.’
