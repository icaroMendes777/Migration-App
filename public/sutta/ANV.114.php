novatos neste Dhamma e Disciplina – devem ser encorajados, exortados e
estabelecidos nessas cinco coisas. Quais cinco? <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’Venham, amigos, sejam
virtuosos. Permaneçam contidos pelas regras do Patimokkha, sejam perfeitos na
