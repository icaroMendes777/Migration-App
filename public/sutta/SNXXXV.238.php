<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
suponham que houvessem quatro cobras venenosas. </span>Então surgisse um homem
que valoriza a vida e teme a morte, que deseja o prazer e abomina a dor<span
lang=PT-BR style='mso-ansi-language:PT-BR'>. Eles dir-lhe-iam ‘Bom homem, estas
quatro cobras são venenosas. De quando em quando elas precisam ser levantadas;
de quando em quando elas precisam ser banhadas; de quando em quando elas
precisam ser alimentadas; de quando em quando elas precisam ser colocadas para
descansar. Mas se alguma dessas cobras em algum momento ficar irritada com
você, então, bom homem, você morrerá ou terá um sofrimento igual à morte. Faça o
que deve ser feito, bom homem!’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
bhikkhus, com medo daquelas quatro cobras venenosas, aquele homem fugiria numa
ou noutra direção. Eles dir-lhe-iam ‘Bom homem, cinco inimigos assassinos
perseguem-no, pensando, “Onde quer que o vejamos, naquele instante o
mataremos.” Faça o que deve ser feito, bom homem!’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
bhikkhus, com medo daquelas quatro cobras venenosas e daqueles cinco inimigos
assassinos, aquele homem fugiria numa ou noutra direção. Eles dir-lhe-iam ‘Bom
homem, um sexto assassino, um companheiro íntimo seu persegue-o com a espada na
mão, pensando, “Onde quer que eu o veja, naquele instante cortarei sua cabeça.”
Faça o que deve ser feito, bom homem!’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
bhikkhus, com medo daquelas quatro cobras venenosas, daqueles cinco inimigos
assassinos e do sexto assassino, um companheiro íntimo dele que o perseguia com
a espada na mão, aquele homem fugiria numa ou noutra direção. Ele veria um
vilarejo despovoado. Qualquer casa que ele entrasse estava vazia, desocupada.
Qualquer pote que ele pegasse estava vazio, oco. Eles dir-lhe-iam ‘Bom homem, a
qualquer momento uma gangue de malfeitores irá atacar este vilarejo despovoado.
Faça o que deve ser feito, bom homem!’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
bhikkhus, com medo daquelas quatro cobras venenosas, daqueles cinco inimigos
assassinos, do sexto assassino, um companheiro íntimo dele que o perseguia com
a espada na mão e da gangue de malfeitores, aquele homem fugiria numa ou noutra
direção. Ele veria uma grande extensão d’água, cuja margem mais próxima fosse
perigosa e aterrorizante e cuja margem mais distante fosse segura e livre de terror,
mas que não houvesse uma balsa ou ponte que levasse até a margem mais distante.
[<a href="#N1">1</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Então, o homem pensaria: ‘Ali está essa grande extensão de água, cuja
margem mais próxima é perigosa e aterrorizante e cuja margem mais distante é
segura e livre de terror, mas não há uma balsa ou ponte que leve até a margem
mais distante. E se eu juntasse capim, gravetos, galhos e folhas e os amarrasse
juntos para fazer uma balsa e suportado pela balsa, fazendo um esforço com as
minhas mãos e pés, eu atravessasse com segurança até a margem mais distante.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então o
homem juntou capim, gravetos, galhos e folhas e os amarrou juntos para fazer
uma balsa e suportado pela balsa, fazendo um esforço com as mãos e pés,
atravessou com segurança até a margem mais distante. Ele cruzou para a outra
margem, foi mais além, o brâmane está em terra firme.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Eu citei esse símile, bhikkhus, para transmitir uma idéia. A idéia é a
seguinte: ‘As quatro cobras venenosas: esse é um termo para os quatro grandes
elementos – o elemento terra, o elemento água, o elemento fogo, o elemento ar. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’Os cinco
inimigos assassinos: esse é um termo para os cinco agregados influenciados pelo
apego; isto é, o agregado da forma material influenciado pelo apego, o agregado
da sensação influenciado pelo apego, o agregado da percepção influenciado pelo
apego, o agregado das formações influenciado pelo apego, o agregado da
consciência influenciado pelo apego.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’O sexto
assassino, o companheiro íntimo com a espada na mão’: esse é um termo para a
cobiça e prazer.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’O
vilarejo despovoado’: esse é um termo para as seis bases dos sentidos internas. Se,
bhikkhus, uma pessoa competente, inteligente e sábia examiná-las através do
olho, elas mostram-se vazias e ocas. Se ela examiná-las através do ouvido ...
através da mente, estas mostram-se vazias e ocas. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’Gangue de
malfeitores’: esse é um termo para as seis bases dos sentidos externas. O olho,
bhikkhus, é atacado por formas agradáveis e desagradáveis. O ouvido ... O nariz
... A língua ... O corpo ... A mente é atacada por objetos mentais agradáveis e
desagradáveis. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’A grande
extensão de água’: esse é um termo para as quatro impurezas: os desejos sensuais,
o ser/existir, as idéias e a ignorância. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’A margem
mais próxima, que é perigosa e aterrorizante’: esse é um termo para a
identidade.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’A margem
mais distante, que é segura e livre de terror’: esse é um termo para Nibbana.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’A balsa’:
esse é um termo para o Nobre Caminho Óctuplo; isto é, entendimento correto ...
concentração correta.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’Fazer
esforço com as mãos e pés’: esse é um termo para a estimulação da energia.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’Ele
cruzou para a outra margem, foi mais além, o brâmane está em terra firme’: esse
é um termo para o arahant.”<o:p></o:p></span></p>

