<p class=Normal> Certa ocasião o venerável Sariputta estava em Magadha, em Nalakagamaka. Então o errante Samandakani foi até o venerável Sariputta e ambos se cumprimentaram. Quando a conversa amigável e cortês havia terminado, ele sentou a um lado e disse: </p>


<p class=Normal> "Amigo, Sariputta, o que é felicidade neste Dhamma e Disciplina, e o que é sofrimento?" </p>


<p class=Normal> "Descontentamento, amigo, neste Dhamma e Disciplina é sofrimento. Contentamento é felicidade. Quando há descontentamento, este sofrimento pode ser esperado. (1) Ao caminhar, não há prazer ou felicidade. (2) Ao ficar em pé ... (3) Ao ficar sentado ... (4) Ao ficar deitado ... (5) Estando no vilarejo ... (6) Estando na floresta ... (7) Estando ao pé de uma árvore ... (8) Estando numa cabana vazia ... (9) Estando a céu aberto ... (10) Estando entre os bhikkhus, não há prazer ou felicidade. Quando há descontentamento, esse sofrimento pode ser esperado. </p>


<p class=Normal> "Quando há contentamento, esta felicidade pode ser esperada. (1) Ao caminhar, há prazer ou felicidade. (2) Ao ficar em pé ... (3) Ao ficar sentado ... (4) Ao ficar deitado ... (5) Estando no vilarejo ... (6) Estando na floresta ... (7) Estando ao pé de uma árvore ... (8) Estando numa cabana vazia ... (9) Estando a céu aberto ... (10) Estando entre os bhikkhus, há prazer ou felicidade. Quando há contentamento, essa felicidade pode ser esperada."
