<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Sentado a
um lado, aquele bhikkhu disse para o Abençoado: “Venerável senhor, qual é a
causa e a razão porque alguém, aqui, com a dissolução do corpo, após a morte,
renasce na companhia dos nagas que nascem de ovos?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
bhikkhu, alguém age de forma ambivalente com o corpo, linguagem e mente. Ele
ouviu: ‘Os nagas nascidos de ovos vivem por muito tempo, são belos e abundam em
felicidade.’ Ele pensa: ‘Oh, que com a dissolução do corpo, após a morte, eu
possa renascer na companhia dos nagas que nascem dos ovos!’ Ele dá alimentos
... Ele dá de beber ... Ele dá roupas ... Ele dá um veículo ... Ele dá uma
grinalda ... Ele dá um perfume ... Ele dá um ungüento ... Ele dá uma moradia
... Ele dá uma lamparina. Então, com a dissolução do corpo, após a morte, ele
renasce na companhia dos nagas que nascem dos ovos.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Essa,
bhikkhu, é a causa e a razão porque alguém, aqui, com a dissolução do corpo,
após a morte, renasce na companhia dos nagas que nascem de ovos.” <b
