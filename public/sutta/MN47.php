<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[1] O bhikkhu que não é capaz de penetrar a
mente do Buda para confirmar que ele é perfeitamente iluminado, tem que chegar
a essa conclusão através da inferência obtida do comportamento corporal e
verbal ou das outras evidências citadas neste sutta. [<a href="#R1">Retorna</a>]<o:p></o:p></span></p>

<p class=Normal><a name=N1A></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[1A]
 Num estudo publicado pelo Venerável Analayo intitulado "The Scope of Free Inquiry" é observado que na versão deste sutta preservado no Madhyama-agama, a expressão "este venerável" aparece desde o início do discurso, no lugar de Tathagata. Esse uso na avaliação de Analayo é extraordinário pois coloca o Buda em pé de igualdade com qualquer outro bhikkhu. Essa expressão "este venerável" contitui portanto uma expressão eloquente do fato que aquilo que está sendo investigado é precisamente a questão se "este venerável" tem as qualidades para ser considerado um Tathagata. [<a href="#R1A">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[2] Ações com o corpo são<span class=GramE><span
style='mso-spacerun:yes'>  </span></span>“estados conscientizados através do olho.”
Palavras são “estados conscientizados através do ouvido.” MA: Da mesma maneira que
se pode inferir a presença de peixes através do ondular e borbulhar da água,
assim também de uma ação ou expressão contaminada, alguém pode inferir que a
mente originária está contaminada. [<a href="#R2">Retorna</a>] <o:p></o:p></span></p>


<p class=Normal><a name=N3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[3] MT: “Estados mesclados”, (<i>vitimissa
dhamma</i>), se refere à conduta de alguém que está empenhado na purificação da
sua conduta<span class=GramE> mas</span> que é incapaz de mantê-la
consistentemente. Algumas vezes a sua conduta é pura ou luminosa, algumas vezes
impura ou obscura. [<a href="#R3">Retorna</a>] <o:p></o:p></span></p>

<p class=Normal><a name=N3A></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[3A] A versão do Madhyama-agama não questiona a quanto tempo o Buda "realizou" a iluminação, mas indica que a investigação deve ser feita se ele "pratica" muito tempo dessa forma ou apenas temporariamente. Portanto na versão em Chinês a questão não é a quanto tempo o Buda realizou a iluminação mas se a sua conduta é consistente. [<a href="#R3A">Retorna</a>] <o:p></o:p></span></p>


<p class=Normal><a name=N4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[4] MA: Os perigos <span class=GramE>são a
presunção</span>, arrogância, etc. Em alguns bhikkhus, enquanto eles não forem
bem conhecidos ou tiverem obtido discípulos, esses perigos não serão enfrentados,
e eles permanecerão calmos e tranqüilos; mas ao se tornarem famosos e com a
obtenção de discípulos, eles passam a se comportar de forma imprópria, atacando
outros bhikkhus como um leopardo ataca um rebanho de gamos. [<a href="#R4">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N5></a><span lang=PT-BR style='mso-ansi-language:
