<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no
Parque de Anathapindika. Lá ele se dirigiu aos monges desta forma: “Bhikkhus” –
“Venerável Senhor,” eles responderam. O Abençoado disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.
“Bhikkhus, um bhikkhu que é um investigador, sem saber como avaliar a mente de
outrem, [<a href="#N1">1</a>] <a name=R1></a>deveria fazer uma investigação do
Tathagata para descobrir se ele é perfeitamente iluminado ou não.<span
class=GramE>”</span><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3.
“Venerável senhor, os nossos ensinamentos têm o Abençoado como origem, como
guia e como refúgio. Seria bom se o Abençoado pudesse explicar o significado
dessas palavras. Tendo ouvido do Abençoado, os bhikkhus o recordarão.<span
class=GramE>”</span><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
bhikkhus, ouçam e prestem muita atenção àquilo que eu vou dizer.” – “Sim,
venerável senhor,” os bhikkhus responderam. O Abençoado disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><a name="T4"></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>4.
“Bhikkhus, um bhikkhu que é um investigador, sem saber como avaliar a mente de
outrem, deveria fazer uma investigação do Tathagata [<a href="#N1A">1A</a>] <a name=R1A></a>com relação a dois tipos de
estados, estados conscientizados através do olho e através do ouvido assim: ‘É ou
não é encontrado no Tathagata algum tipo de estado contaminado conscientizado
através do olho ou através do <span class=GramE>ouvido?</span>’ [<a href="#N2">2</a>]
<a name=R2></a>Ao investigá-lo, ele se dá conta de que: ‘Não são encontrados no
Tathagata estados contaminados conscientizados através do olho ou através do
ouvido.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. “Ao
compreender isso, ele o investiga mais deste modo: ‘É ou não é encontrado no
Tathagata algum tipo de estado mesclado conscientizado através do olho ou através do
<span class=GramE>ouvido?</span>’ [<a href="#N3">3</a>] <a name=R3></a>Ao
investigá-lo, ele se dá conta de que: ‘Não são encontrados no Tathagata estados
mesclados conscientizados através do olho ou através do ouvido.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. “Ao
compreender isso, ele o investiga mais deste modo: ‘São ou não são encontrados
no Tathagata estados purificados conscientizados através do olho ou através do <span
class=GramE>ouvido?</span>’ Ao investigá-lo, ele se dá conta de que: ‘São
encontrados no Tathagata estados purificados conscientizados através do olho ou
através do ouvido.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. “Ao
compreender isso, ele o investiga mais deste modo: ‘Este venerável alcançou
este estado benéfico faz muito tempo ou ele o alcançou <span class=GramE>recentemente?</span>’
Ao investigá-lo, ele se dá conta que: ‘Este venerável alcançou este estado
benéfico faz muito tempo; ele não o alcançou apenas recentemente.’ [<a href="#N3A">3A</a>] <a name=R3A></a><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. “Ao
compreender isso, ele o investiga mais deste modo: ‘Este venerável adquiriu
renome e alcançou a fama, e como resultado disso, os perigos [conectados com o
renome e a fama] são encontrados <span class=GramE>nele?</span>’ Pois,
bhikkhus, enquanto um bhikkhu não houver adquirido renome e alcançado a fama, ele
não enfrentará esses perigos [conectados com o renome e a fama]; mas quando ele
houver adquirido renome e alcançado a fama, ele enfrentará esses perigos.[<a
href="#N4">4</a>] <a name=R4></a>Ao investigá-lo, ele se dá conta de que: ‘Este
venerável adquiriu renome e alcançou a fama, mas os perigos [conectados com o
renome e a fama] não são encontrados nele.’” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. “Ao
compreender isso, ele o investiga mais deste modo: ‘Este venerável está contido
sem temor, não contido pelo temor, e ele evita entregar-se aos prazeres
sensuais porque ele está isento de cobiça devido à destruição da <span
class=GramE>cobiça?</span>’ Ao investigá-lo, ele se dá conta de que: ‘Este
venerável está contido sem temor, não contido pelo temor, e ele evita entregar-se
aos prazeres sensuais porque ele está isento de cobiça devido à destruição da
cobiça.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. “Agora,
bhikkhus, se outros perguntarem<span class=GramE><span
style='mso-spacerun:yes'>  </span></span>àquele bhikkhu assim: ‘Quais são os
motivos do venerável e qual é a prova através da qual ele diz: “Aquele
venerável está contido sem temor, não contido pelo temor, e ele evita
entregar-se aos prazeres sensuais porque ele está isento de cobiça devido à
destruição da cobiça”?’ – respondendo da forma correta, aquele bhikkhu
responderia assim: ‘Quer aquele venerável esteja com a Sangha ou só, enquanto
alguns ali se comportam bem e alguns se comportam mal e alguns ensinam um
grupo, [<a href="#N5">5</a>] <a name=R5></a>enquanto alguns estão preocupados
com coisas materiais e alguns não estão corrompidos pelas coisas materiais, não
obstante, aquele venerável não despreza ninguém por conta disso. [<a href="#N6">6</a>]
<a name=R6></a>E eu ouvi e aprendi isto da própria boca do Abençoado: “Eu estou
contido sem temor, não contido pelo temor, e evito entregar-me aos prazeres
sensuais porque estou isento de cobiça devido à destruição da cobiça.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. “Além
disso, bhikkhus, o Tathagata deveria ser questionado sobre isso, assim: ‘É, ou
não é, encontrado no Tathagata algum tipo de estado contaminado conscientizado através
do olho ou através do <span class=GramE>ouvido?</span>’ O Tathagata responderia
assim: ‘No Tathagata não é encontrado nenhum tipo de estado contaminado
conscientizado através do olho ou através do ouvido.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. “Se for
perguntado, ‘É, ou não é, encontrado no Tathagata algum tipo de estado mesclado
conscientizado através do olho ou através do <span class=GramE>ouvido?</span>’ O
Tathagata responderia assim: ‘Não é encontrado no Tathagata nenhum tipo de
estado mesclado conscientizado através do olho ou através do ouvido.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. “Se for
perguntado,<span class=GramE><span style='mso-spacerun:yes'>  </span></span>‘São,
ou não são, encontrados no Tathagata estados purificados conscientizados através do
olho ou através do ouvido?’ O Tathagata responderia assim: ‘No Tathagata são
encontrados estados purificados conscientizados através do olho ou através do
ouvido. Esses são o meu caminho e o meu domínio, no entanto eu não me
identifico com eles.’ [<a href="#N7">7</a>] <o:p></o:p></span></p>



<p class=Normal><a name=R7></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>14. “Bhikkhus, um discípulo deveria se aproximar do Mestre, que assim
fala, para ouvir o Dhamma. O Mestre lhe ensina o Dhamma em seus níveis cada vez
mais elevados, em seus níveis cada vez mais sublimes, com as suas contrapartes
luminosas e obscuras. À medida que o Mestre vai ensinando o Dhamma para um
bhikkhu dessa forma, através do conhecimento direto de <span class=GramE>um
certo</span> ensinamento desse Dhamma, o bhikkhu chega a uma conclusão sobre os
ensinamentos. Ele deposita confiança no Mestre assim: ‘O Abençoado é
perfeitamente iluminado, o Dhamma é bem proclamado pelo Abençoado, a Sangha
pratica o bom caminho.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. “Agora,
se outros perguntarem ao bhikkhu assim: ‘Quais são os motivos do venerável e
qual é a prova através da qual ele diz: “O Abençoado é perfeitamente iluminado,
o Dhamma é bem proclamado pelo Abençoado, a Sangha pratica o bom caminho<span
class=GramE>”?</span>’ - respondendo da forma correta, aquele bhikkhu
responderia assim: ‘Aqui, amigos, eu me aproximei do Abençoado para ouvir o
Dhamma. O Abençoado me ensinou o Dhamma em seus níveis cada vez mais elevados,
em seus níveis cada vez mais sublimes, com as suas contrapartes luminosas e obscuras.
À medida que o Abençoado foi me ensinando<span style='mso-spacerun:yes'> 
</span>o Dhamma dessa forma, através do conhecimento direto de um certo
ensinamento nesse Dhamma, eu cheguei a uma conclusão sobre os ensinamentos. Eu
deposito confiança no Abençoado assim: ‘O Abençoado é perfeitamente iluminado,
o Dhamma é bem proclamado pelo Abençoado, a Sangha pratica o bom caminho.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16.
“Bhikkhus, quando a fé de alguém tiver sido bem plantada, enraizada e
estabelecida no Tathagata com base nesses motivos, termos e frases, a fé dele
diz-se que é suportada por motivos, enraizada na visão, firme; [<a href="#N8">8</a>]<a
name=R8></a> <a name=R9></a>ela não pode ser derrotada por nenhum contemplativo
ou brâmane, ou <span class=GramE>deva,</span> ou Mara, ou Brahma, ou qualquer
um neste mundo. Assim, bhikkhus, é como<span style='mso-spacerun:yes'> 
</span>ocorre uma investigação do Tathagata de acordo com o Dhamma, e assim é
como o Tathagata é bem investigado de acordo com o Dhamma.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o
que disse o Abençoado. Os bhikkhus ficaram satisfeitos e contentes com as
palavras do Abençoado.<o:p></o:p></span></p>

