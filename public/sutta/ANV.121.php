<p class=Normal> Em certa ocasião o Abençoado estava em Vesali na Grande Floresta no Salão com um pico na cumeeira. Então ao anoitecer o Abençoado saiu do seu isolamento e foi até a enfermaria onde ele viu um certo bhikkhu fraco e enfermo. Ele então sentou num assento que havia sido preparado e se dirigiu aos bhikkhus:</p>

<p class=Normal> "Bhikkhus, se cinco coisas não escapulirem de um bhikkhu fraco e enfermo, pode-se esperar dele: 'Em pouco tempo, realizando por si mesmo através do conhecimento direto aqui e agora, ele entrará e permanecerá na libertação da mente e libertação através da sabedoria que são imaculadas com a destruição de todas as impurezas.' Quais cinco? Aqui, um bhikkhu permanece contemplando as coisas repulsivas no corpo, percebendo as coisas repulsivas na comida, percebendo o não-deleite com tudo no mundo, contemplando a impermanência em todas as formações condicionadas; e ele tem a percepção da morte bem estabelecida internamente. Se essas cinco coisas não escapulirem de um bhikkhu fraco e enfermo, pode-se esperar dele: 'Em pouco tempo, realizando por si mesmo através do conhecimento direto aqui e agora, ele entrará e permanecerá na libertação da mente e libertação através da sabedoria que são imaculadas com a destruição de todas as impurezas.'"</p>

