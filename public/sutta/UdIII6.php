<p class=Normal>Assim ouvi. Em certa ocasião, o Abençoado estava em Rajagaha, no Bambual, no Santuário dos Esquilos. </p>



<p class=Normal>Agora, naquela ocasião o venerável Pilindavaccha havia adquirido o hábito de ofender outros bhikkhus dirigindo-se a eles como se fossem párias, vassalos. </p>



<p class=Normal>Então vários bhikkhus foram ao Abençoado, o homenagearam e sentaram-se a um lado. Sentados eles disseram ao Abençoado: "Senhor, o venerável Pilindavaccha ofende outros bhikkhus dirigindo-se a eles como se fossem párias, vassalos.”</p>



<p class=Normal>O Abençoado então dirigiu-se a um certo bhikkhu dizendo: "Bhikkhu, vá  até o venerável Pilindavaccha e diga que o Mestre o chama.”</p>



<p class=Normal>"Sim, venerável senhor," disse o bhikkhu que em seguida foi até o venerável Pilindavaccha e disse: "O Mestre o chama, venerável amigo.”</p>



<p class=Normal>"Sim, amigo" respondeu o venerável Pilindavaccha, que em seguida foi até o Abençoado. Após homenageá-lo, ele sentou-se a um lado. Enquanto estava sentado o Abençoado dirigiu-se a ele dizendo: "É verdade, Pilindavaccha, que você ofende outros bhikkhus dirigindo-se a eles como se fossem párias, vassalos?”</p>



<p class=Normal>"Sim, venerável senhor" respondeu Pilindavaccha.</p>



<p class=Normal>Em seguida, tendo o Abençoado direcionado sua atenção para as vidas passadas do venerável Pilindavaccha, disse para os bhikkhus "Não se ofendam com o bhikkhu Vaccha. Não é motivado por ódio que ele ofende outros bhikkhus, dirigindo-se a eles como se fossem párias. Ao longo de quinhentos renascimentos ele renasceu em famílias brâmanes. Ao longo de muito tempo ele adquiriu o costume de dirigir-se aos demais como párias, vassalos. Esta é a razão pela qual ele agora dirige-se aos demais bhikkhus como se fossem párias, vassalos.”</p>



<p class=Normal>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p>



	<p class=Normal style='margin-left:.75in'>"Livre do ódio e da presunção,<br>  

	em quem cessou a cobiça e a paixão,<br> 

	desinteressado, desapegado, livre do desejos,<br>

	desprovido de raiva, completamente apaziguado,<br> 

	ele é um brâmane, um asceta, um contemplativo.”</p>


