<p class=Normal>"Bhikkhus, (1) para uma pessoa imoral, alguém deficiente no comportamento virtuoso, (2) a libertação do arrependimento carece a sua condição. Quando não há a libertação do arrependimento, alguém deficiente na libertação do arrependimento, (3) a satisfação carece a sua condição. Quando não há satisfação, alguém deficiente na satisfação, (4) o êxtase carece a sua condição. Quando não há êxtase, alguém deficiente no êxtase, (5) a calma carece a sua condição. Quando não há calma, alguém deficiente na calma, (6) a felicidade carece a sua condição. Quando não há felicidade, alguém deficiente na felicidade, (7) a concentração carece a sua condição. Quando não há concentração, alguém deficiente na concentração, (8) o conhecimento e visão de como as coisas na verdade são carece a sua condição. Quando não há conhecimento e visão de como as coisas na verdade são, alguém deficiente no conhecimento e visão de como as coisas na verdade são, (9) o desencantamento e o desapego carece a sua condição. Quando não há desencantamento e desapego, alguém deficiente no desencantamento e desapego, (10) o conhecimento e visão da libertação carece a sua condição. </p>

<p class=Normal>"Suponham uma árvore desprovida de galhos e folhas, os seus brotos não irão amadurecer, nem a sua casca, nem o seu alburno, nem o seu cerne; assim também, para uma pessoa imoral, alguém deficiente no comportamento virtuoso, a libertação do arrependimento carece a sua condição. Quando não há a libertação do arrependimento ... o conhecimento e visão da libertação carece a sua condição. </p>

<p class=Normal>"Bhikkhus, (1) para uma pessoa virtuosa, alguém cujo comportamento é virtuoso, (2) a libertação do arrependimento possui a sua condição. Quando há a libertação do arrependimento, alguém que possui a libertação do arrependimento, (3) a satisfação possui a sua condição. Quando há satisfação, alguém que possui satisfação, (4) o êxtase possui a sua condição. Quando há êxtase, alguém que possui êxtase, (5) a calma possui a sua condição. Quando há calma, alguém que possui calma, (6) a felicidade possui a sua condição. Quando há felicidade, alguém que possui felicidade, (7) a concentração possui a sua condição. Quando há concentração, alguém que possui concentração, (8) o conhecimento e visão de como as coisas na verdade são possui a sua condição. Quando há conhecimento e visão de como as coisas na verdade são, alguém que possui conhecimento e visão de como as coisas na verdade são, (9) o desencantamento e o desapego possui a sua condição. Quando há desencantamento e desapego, alguém que possui desencantamento e desapego, (10) o conhecimento e visão da libertação possui a sua condição.</p>

<p class=Normal>"Suponham uma árvore provida de galhos e folhas, os seus brotos irão amadurecer, também a sua casca, também o seu alburno, também o seu cerne; assim também, para uma pessoa virtuosa, alguém cujo comportamento é virtuoso, a libertação do arrependimento possui a sua condição. Quando há a libertação do arrependimento ... o conhecimento e visão da libertação possui a sua condição." </p>

