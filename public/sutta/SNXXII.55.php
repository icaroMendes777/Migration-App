<p class=Normal> Em Savatthi. Lá o Abençoado disse esta exposição inspirada: ‘Isso poderá não ser; isso poderá não ser meu. Isso não será; isso não será meu': assim determinado, um bhikkhu poderá remover os cinco primeiros grilhões.'" [<a href="#N1">1</a>]</p>

<p class=Normal><a name=R1></a> Quando isso foi dito, um certo bhikkhu disse para o Abençoado: "Mas como, venerável senhor, um bhikkhu poderá assim determinado: ‘Isso poderá não ser; isso poderá não ser meu. Isso não será; isso não será meu', remover os cinco primeiros grilhões?" </p>

<p class=Normal> "Aqui, bhikkhu, a pessoa comum sem instrução, que não respeita os nobres ... considera a forma como sendo o eu ... ou o eu na consciência. </p>

<p class=Normal> "Ela não compreende como na verdade é a forma impermanente como 'forma impermanente' ... sensação impermanente como 'sensação impermanente' ... percepção impermanente como 'percepção impermanente' ... formações volitivas impermanentes como 'formações volitivas impermanentes' ... consciência impermanente como 'consciência impermanente'. </p>

<p class=Normal> "Ela não compreende como na verdade é a forma dolorosa como 'forma dolorosa' ... sensação dolorosa como 'sensação dolorosa' ... percepção dolorosa como 'percepção dolorosa' ... formações volitivas dolorosas como 'formações volitivas dolorosas' ... consciência dolorosa como 'consciência dolorosa'. </p>

<p class=Normal> "Ela não compreende como na verdade é a forma como não-eu como 'forma como não-eu' ... sensação como não-eu como 'sensação como não-eu' ... percepção como não-eu como 'percepção como não-eu' ... formações volitivas como não-eus como 'formações volitivas como não-eus' ... consciência como não-eu como 'consciência como não-eu'. </p>

<p class=Normal> "Ela não compreende como na verdade é a forma condicionada como 'forma condicionada' ... sensação condicionada como 'sensação condicionada' ... percepção condicionada como 'percepção condicionada' ... formações volitivas condicionadas como 'formações volitivas condicionadas' ... consciência condicionada como 'consciência condicionada'. </p>

<p class=Normal> "Ela não compreende como na verdade é a forma será exterminada como 'forma será exterminada' ... sensação será exterminada como 'sensação será exterminada' ... percepção será exterminada como 'percepção será exterminada' ... formações volitivas serão exterminadas como 'formações volitivas serão exterminadas' ... consciência será exterminada como 'consciência será exterminada'. </p>

<p class=Normal> "O nobre discípulo bem instruído, bhikkhu, que respeita os nobres ... não considera a forma como sendo o eu ... ou o eu na consciência. </p>

<p class=Normal> "Ele compreende como na verdade é a forma impermanente como 'forma impermanente' ... sensação impermanente como 'sensação impermanente' ... percepção impermanente como 'percepção impermanente' ... formações volitivas impermanentes como 'formações volitivas impermanentes' ... consciência impermanente como 'consciência impermanente'. </p>

<p class=Normal> "Ele compreende como na verdade é a forma dolorosa como 'forma dolorosa' ... sensação dolorosa como 'sensação dolorosa' ... percepção dolorosa como 'percepção dolorosa' ... formações volitivas dolorosas como 'formações volitivas dolorosas' ... consciência dolorosa como 'consciência dolorosa'. </p>

<p class=Normal> "Ele compreende como na verdade é a forma como não-eu como 'forma como não-eu' ... sensação como não-eu como 'sensação como não-eu' ... percepção como não-eu como 'percepção como não-eu' ... formações volitivas como não-eus como 'formações volitivas como não-eus' ... consciência como não-eu como 'consciência como não-eu'. </p>

