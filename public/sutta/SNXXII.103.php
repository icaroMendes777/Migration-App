<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, há essas quatro partes. Quais quatro? A parte da
identidade, a parte da origem da identidade, a parte da cessação da identidade,
a parte do caminho que conduz à cessação da identidade. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é a parte da identidade? Deve ser dito: os cinco agregados
influenciados pelo apego. Quais cinco? O agregado da forma influenciado pelo
apego, o agregado da sensação influenciado pelo apego, o agregado da percepção
influenciado pelo apego, o agregado das formações volitivas influenciado pelo apego, o
agregado da consciência influenciado pelo apego. Essa é chamada a parte da
identidade. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual, bhikkhus,
é a parte da origem da identidade? É o desejo que conduz a uma renovada existência, acompanhado 
pela cobiça e pelo prazer, buscando o prazer aqui e ali; isto é, o desejo pelos prazeres sensuais, 
o desejo por ser/existir, o desejo por não ser/existir.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é a parte da cessação da identidade? É o desaparecimento e cessação sem deixar vestígios 
daquele mesmo desejo, abrir mão, descartar, libertar-se, despegar desse mesmo desejo. Essa é chamada a
parte da cessação da identidade.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é a parte do caminho que conduz à cessação da identidade? É este
Nobre Caminho Óctuplo; isto é, entendimento correto ... concentração correta.
Essa é chamada a parte do caminho que conduz à cessação da identidade.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Essas,
bhikkhus, são as quatro partes.”<b style='mso-bidi-font-weight:normal'><o:p></o:p></b></span></p>


