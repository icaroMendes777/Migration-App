<p class=Normal> “Bhikkhus, quando um discípulo leigo possui oito qualidades, a Sangha, se assim desejar, poderá virar as tigelas de esmolar de boca para baixo. Quais oito? (1) Ele tenta evitar que os bhikkhus recebam oferendas; (2) ele tenta prejudicar os bhikkhus; (3) ele tenta evitar que os bhikkhus residam num determinado lugar; (4) ele insulta e ofende os bhikkhus; (5) ele causa um cisma entre os bhikkhus; (6) ele fala com desprezo do Buda; (7) ele fala com desprezo do Dhamma; (8) ele fala com desprezo da Sangha. Quando um discípulo leigo possui essas oito qualidades, a Sangha, se assim desejar, poderá virar as tigelas de esmolar de boca para baixo.</p>

<p class=Normal> “Bhikkhus, quando um discípulo leigo possui oito qualidades, a Sangha, se assim desejar, poderá virar as tigelas de esmolar de boca para cima. Quais oito? (1) Ele não tenta evitar que os bhikkhus recebam oferendas; (2) ele não tenta prejudicar os bhikkhus; (3) ele não tenta evitar que os bhikkhus residam num determinado lugar; (4) ele não insulta e ofende os bhikkhus; (5) ele não causa um cisma entre os bhikkhus; (6) ele não fala com desprezo do Buda; (7) ele não fala com desprezo do Dhamma; (8) ele não fala com desprezo da Sangha. Quando um discípulo leigo possui essas oito qualidades, a Sangha, se assim desejar, poderá virar as tigelas de esmolar de boca para cima."
</p>

