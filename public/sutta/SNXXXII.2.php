<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'> “Essa, bhikkhu, é a causa e a razão  porque alguém aqui, com a dissolução do corpo, após a morte, renasce na companhia dos devas que habitam as nuvens.”<b
