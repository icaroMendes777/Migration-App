<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em certa
ocasião, o Venerável Anuruddha estava<span style='mso-spacerun:yes'>  </span>em
Savatthi à margem do Sutanu. Então, um grande número de bhikkhus foram até o
Venerável Anuruddha e eles se cumprimentaram. Quando a conversa cortês e
amigável havia terminado, eles sentaram a um lado e disseram para o Venerável
Anuruddha:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Por ter
desenvolvido e cultivado quais coisas é que o Venerável Anuruddha alcançou a
supremacia do conhecimento direto?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigos, é
porque eu desenvolvi e cultivei os quatro fundamentos da atenção plena que
alcancei a supremacia do conhecimento direto. Quais quatro? Aqui, amigos, eu
permaneci contemplando o corpo como um corpo, ardente, plenamente consciente e
com atenção plena, tendo colocado de lado a cobiça e o desprazer pelo mundo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu
permaneci contemplando as sensações como sensações ... mente como mente ... objetos
mentais como objetos mentais, ardente, plenamente consciente e com atenção
plena, tendo colocado de lado a cobiça e o desprazer pelo mundo.<o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Amigos, é porque eu desenvolvi e cultivei os quatro fundamentos da
atenção plena que alcancei a supremacia do conhecimento direto. Além disso,
amigos, é porque eu desenvolvi e cultivei os quatro fundamentos da atenção
plena que compreendo as mentes de outros seres, de outras pessoas, abarcando-as
com a minha própria mente. Eu compreendo uma mente afetada pelo desejo como
afetada pelo desejo e uma mente não afetada pelo desejo como não afetada pelo
desejo; Eu compreendo uma mente afetada pela raiva como afetada pela raiva e
uma mente não afetada pela raiva como não afetada pela raiva; Eu compreendo uma
mente afetada pela delusão como afetada pela delusão e uma mente não afetada
pela delusão como não afetada pela delusão; Eu compreendo uma mente contraída
como contraída e uma mente distraída como distraída; Eu compreendo uma mente
transcendente como transcendente e uma mente não transcendente como não
transcendente; Eu compreendo uma mente superável como superável e uma mente não
superável como não superável; Eu compreendo uma mente concentrada como
concentrada e uma mente não concentrada como não concentrada; Eu compreendo uma
mente libertada como libertada e uma mente não libertada como não libertada.”<o:p></o:p></span></p>

