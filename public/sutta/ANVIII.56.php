<p class=Normal> "Bhikkhus, (1) 'medo' é uma designação para os prazeres sensuais. (2) 'Sofrimento' é uma designação para os prazeres sensuais. (3) 'Enfermidade' é uma designação para os prazeres sensuais. (4) 'Abscesso' é uma designação para os prazeres sensuais. (5) 'Flecha' é uma designação para os prazeres sensuais. (6) 'Grude' é uma designação para os prazeres sensuais. (7) 'Pântano' é uma designação para os prazeres sensuais. (8) 'Útero' é uma designação para os prazeres sensuais. </p>

<p class=Normal> "Por que, bhikkhus, 'perigo' é uma designação para os prazeres sensuais? Alguém excitado pelo desejo sensual, atado pelo desejo e paixão, não está livre do perigo nesta vida, ou do perigo em vidas futuras; assim 'perigo' é uma designação para os prazeres sensuais. </p>

<p class=Normal> "Por que 'sofrimento' ... 'enfermidade' ... 'abscesso' ... 'flecha' ... 'grude' ... 'pântano' ... 'útero' é uma designação para os prazeres sensuais? Alguém excitado pelo desejo sensual, atado pelo desejo e paixão, não está livre do útero nesta vida, ou do útero em vidas futuras; assim ' útero ' é uma designação para os prazeres sensuais." </p>

<p class=Normal> Perigo, sofrimento, e enfermidade, <br>
uma abscesso, uma flecha, e um grude, <br>
um pântano e um útero: <br>
estes descrevem os prazeres sensuais<br>
aos quais o mundano está apegado. <br>
Estando imerso no prazeroso, <br>
ele novamente retorna ao útero. </p>

<p class=Normal> Mas quando um bhikkhu é ardente<br>
e não negligencia a plena consciência, <br>
desse modo ele transcende<br>
esse atoleiro miserável; <br>
ele inspeciona esta população trêmula<br>
que caiu para o nascimento e envelhecimento.
</p>

