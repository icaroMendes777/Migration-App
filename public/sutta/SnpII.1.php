<p class=Normal style='margin-left:.75in'> 

Todos os espíritos aqui reunidos,<br>
&nbsp;&nbsp;&nbsp; - na terra, ou no céu - <br>
que todos estejam em paz<br>
e ouçam atentamente o que tenho a dizer. <o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>

Portanto, espíritos, prestem atenção.<br>
Que vocês possam difundir o amor bondade para esses seres humanos<br>
que dia e <span style="mso-spacerun: yes"> </span>noite trazem oferendas para vocês,<br>
assim, com diligência, protejam-los. <o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>

Toda riqueza - aqui ou no além - <br>
todos os tesouros preciosos do paraíso,<br>
para nós, não se igualam ao Tathagata.<br>
&nbsp;&nbsp;&nbsp; Essa jóia preciosa é o Buda.<br>
&nbsp;&nbsp;&nbsp; Por esta afirmação da verdade, possa a felicidade existir. <o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>

O Nibbana supremo - cessação, desapego - <br>
descoberto pelo Sábio dos Sakias através da meditação:<br>
Não existe nada que possa se igualar a esse Dhamma.<br>
&nbsp;&nbsp;&nbsp; Essa jóia preciosa é o Dhamma.<br>
&nbsp;&nbsp;&nbsp; Por esta afirmação da verdade, possa a felicidade existir. <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>

O supremo Desperto exaltou o caminho da purificação (o Nobre Caminho
Óctuplo),<br>
chamando-o de o caminho infalível para a concentração, <br>
o conhecimento sem intermediários:<br>
Não pode ser encontrado nada igual a essa concentração.<br>
&nbsp;&nbsp;&nbsp;Essa jóia preciosa é o Dhamma.<br>
&nbsp;&nbsp;&nbsp;Por esta afirmação da verdade, possa a felicidade existir. <o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>

Os oito indivíduos que constituem quatro pares, <br>
elogiados pelos homens virtuosos:<br>
Eles, discípulos do Abençoado, merecem oferendas.<br>
O que se lhes dá, produz grandes frutos.<br>
&nbsp;&nbsp;&nbsp;Essa jóia preciosa é a Sangha.<br>
&nbsp;&nbsp;&nbsp;Por esta afirmação da verdade, possa a felicidade existir. <o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>

Aqueles que, devotos, determinados,<br>
se aplicam aos ensinamentos de Gotama,<br>
e tendo alcançado o objetivo, mergulham no Nibbana,<br>
desfrutando livremente a Libertação que conquistaram.<br>
&nbsp;&nbsp;&nbsp;Essa jóia preciosa é a Sangha.<br>
&nbsp;&nbsp;&nbsp;Por esta afirmação da verdade, possa a felicidade existir.<span
style="mso-spacerun: yes">  </span><o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>

Uma pilastra de Indra,[<a href="#N1">1</a>] <a name=R1></a>fincada na terra,<br>
que mesmo os ventos dos quatro quadrantes não conseguem sacudir:<br>
assim é, eu lhes digo, uma pessoa íntegra,<br>
que - tendo compreendido<br>
as nobres verdades - vê.<br>
&nbsp;&nbsp;&nbsp;Essa jóia preciosa é a Sangha.<br>
&nbsp;&nbsp;&nbsp;Por esta afirmação da verdade, possa a felicidade existir. <o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>

Quem vê as nobres verdades<br>
bem ensinadas por aquele que possui profunda sabedoria - <br>
independente do que (mais tarde) o faça ser negligente - <br>
não irá ter mais do que sete existências. [<a href="#N2">2</a>]<br>
&nbsp;&nbsp;&nbsp;<a name=R2></a>Essa jóia preciosa é a Sangha.<br>
&nbsp;&nbsp;&nbsp;Por esta afirmação da verdade, possa a felicidade existir. <o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>

No momento em que é realizado o olho do Dhamma<br>
três coisas são abandonadas:<br>
&nbsp;&nbsp;&nbsp; idéias acerca da identidade, dúvidas, <br>
&nbsp;&nbsp;&nbsp; e todo apego a preceitos e rituais. [<a
href="#N3">3</a>]<br>
<a name=R3></a>Ele está libertado <br>
dos quatro estados miseráveis, [<a href="#N4">4</a>]<br>
<a name=R4></a>e incapaz de cometer<br>
as seis grandes ofensas. [<a href="#N5">5</a>] <br>
&nbsp;&nbsp;&nbsp;<a name=R5></a>Essa jóia preciosa é a Sangha.<br>
&nbsp;&nbsp;&nbsp;Por esta afirmação da verdade, possa a felicidade existir. <o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>

Qualquer ação ruim e prejudicial que seja cometida<br>
- com o corpo, linguagem ou mente -<br>
ele não será capaz de escondê-la:<br>
uma incapacidade atribuída<br>
àqueles que alcançaram o Caminho (Supramundano).<br>
&nbsp;&nbsp;&nbsp;Essa jóia preciosa é a Sangha.<br>
&nbsp;&nbsp;&nbsp;Por esta afirmação da verdade, possa a felicidade existir. <o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>

Tal como um bosque com os topos em florescência<br>
no primeiro mês do calor do verão,<br>
assim é o Dhamma insuperável que ele ensinou,<br>
para o benefício supremo que conduz ao Nibbana.<br>
&nbsp;&nbsp;&nbsp;Essa jóia preciosa é o Buda.<br>
&nbsp;&nbsp;&nbsp;Por esta afirmação da verdade, possa a felicidade existir. <o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>

Supremo (o Buda),<br>
Conhecedor (de Nibbana),<br>
Provedor (de Nibbana),<br>
Trazedor ( do Nobre Caminho Óctuplo),<br>
&nbsp;&nbsp;&nbsp; ele ensinou o<br>
insuperável Dhamma.<br>
&nbsp;&nbsp;&nbsp;Essa jóia preciosa é o Buda.<br>
&nbsp;&nbsp;&nbsp;Por esta afirmação da verdade, possa a felicidade existir. <o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>

Deram fim ao (kamma) passado, novo kamma não mais surge.<br>
Com a mente desapegada do devir,<br>
a semente (consciência de renascimento) morreu, eles não têm desejo de
renascer,<br>
esses sábios, se extinguem como a chama desta lamparina.<br>
&nbsp;&nbsp;&nbsp;Essa jóia preciosa é a Sangha.<br>
&nbsp;&nbsp;&nbsp;Por esta afirmação da verdade, possa a felicidade existir. <o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>

Todos espíritos aqui reunidos,<br>
&nbsp;&nbsp;&nbsp; - na terra, no céu - <br>
prestemos homenagem ao Buda,<br>
o Tathagata venerado por seres<br>
humanos e divinos.<br>
&nbsp;&nbsp;&nbsp; Possa haver<br>
&nbsp;&nbsp;&nbsp; felicidade. <o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>

Todos espíritos aqui reunidos,<br>
&nbsp;&nbsp;&nbsp; - na terra, no céu - <br>
prestemos homenagem ao Dhamma<br>
e ao Tathagata venerado por seres<br>
humanos e divinos.<br>
&nbsp;&nbsp;&nbsp; Possa haver<br>
&nbsp;&nbsp;&nbsp; felicidade. <o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR>Todos espíritos aqui reunidos,<br>
&nbsp;&nbsp;&nbsp; - na terra, no céu - <br>
prestemos homenagem à Sangha<br>
e ao Tathagata venerado por seres<br>
humanos e divinos.<br>
&nbsp;&nbsp;&nbsp; Possa haver<br>
&nbsp;&nbsp;&nbsp; felicidade.</span></p>


