<p class=Normal><a name=R1></a> Em Savatthi. “Bhikkhus, eu ensinarei para vocês a agitação devido ao apego e a não agitação devido ao não apego. [<a href="#N1">1</a>] Ouçam e prestem muita atenção àquilo que eu vou dizer.” – “Sim, venerável senhor,” os bhikkhus responderam. O Abençoado disse o seguinte: </p>

<p class=Normal> <a name=R2></a> “Porque, amigos, existe agitação devido ao apego? Neste caso, uma pessoa comum sem instrução que não respeita os nobres, que não é proficiente nem treinada no Dhamma deles, que não respeita os homens verdadeiros, que não é proficiente nem treinada no Dhamma deles, considera a forma material assim: 'Isso é meu, isso sou eu, isso é o meu eu'. [<a href="#N2">2</a>] Essa forma material muda e se altera. Com a forma material mudando e se alterando, a tristeza, lamentação, dor, angústia e desespero surgem nela. </p>

<p class=Normal> “Ela considera a sensação assim … considera a percepção assim ...  considera as formações volitivas assim … considera a consciência assim: 'Isso é meu, isso sou eu, isso é o meu eu' Essa consciência muda e se altera. Com a consciência mudando e se alterando, a tristeza, lamentação, dor, angústia e desespero surgem nela. </p>

<p class=Normal> “E porque, amigos, existe a não agitação devido ao não apego? Neste caso um nobre discípulo bem instruído, que respeita os nobres, que é proficiente e treinado no Dhamma deles, que respeita os homens verdadeiros, que é proficiente e treinado no Dhamma deles, não considera a forma material assim: 'Isso é meu, isso sou eu, isso é o meu eu'  Essa forma material muda e se altera. Com a forma material mudando e se alterando, a tristeza, lamentação, dor, angústia e desespero não surgem nele. </p>


<p class=Normal> “Ele não considera a sensação assim … não considera a percepção assim ... não considera as formações volitivas assim … não considera a consciência assim: 'Isso é meu, isso sou eu, isso é o meu eu' Essa consciência muda e se altera. Com a consciência mudando e se alterando, a tristeza, lamentação, dor, angústia e desespero não surgem nele.</p>

