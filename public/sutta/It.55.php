<p class=Normal>Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: “Há esses três tipos de buscas. Quais três? A busca pelo prazer sensual, a busca por ser/existir e a busca por uma vida santa. Essas são as três buscas.” </p>

<p class=Normal style='margin-left:.75in'>A busca pelo prazer sensual, a busca por ser/existir,<br>
