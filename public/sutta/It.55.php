e nelas se agarra como se fossem a verdade -<br>
isso é uma acumulação de contaminações. </p>

<p class=Normal style='margin-left:.75in'>Num bhikkhu completamente desapegado, <br>
libertado com a destruição do desejo, <br>
as buscas foram abandonadas<br>
e os pontos de vista desenraizados. <br>
Com a destruição das buscas, um bhikkhu<br>
está livre do desejo e da dúvida. </p>

