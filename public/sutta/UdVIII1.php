envolvem os sentidos. O segundo verbo, <span class=SpellE><i style='mso-bidi-font-style:
normal'>atthi</i></span>, se refere às qualidades transcendentes da existência,
o incondicionado ou a não manifesta natureza da mente.” Neste sutta o verbo
empregado é <span class=SpellE><i style='mso-bidi-font-style:normal'>atthi</i></span>.
Com relação à natureza da mente, veja o <a href="ANI.49-52.php">AN I.49-52</a>.</p>
<p class=Normal>O Bhikkhu  Ñananada no livro <i>Concept and Reality</i> explica este trecho da seguinte forma: os termos "terra, água, fogo, nem ar; espaço infinito, consciência infinita, nada, nem percepção, nem não percepção" não devem ser interpretados sob a perspectiva física grosseira mas sim sob a perspectiva de conceitos. A referência a "este mundo, outro mundo, sol, lua, não há vir, nem ir, nem permanecer; não há morte nem renascimento" são também referências a noções abstratas e não fatos implícitos. Essas são noções abstratas que são um elemento importante no nosso mundo de conceitos relativos. Os últimos três termos: "não há base, não há evolução e não há suporte" corrrespondem respectivamente aos três modos de realizar a libertação (veja <a href="../glossario.php#vimokkha"><i>vimokkha</i></a> no glossário): <i>suññato</i> (vazio), <i>appanihito</i> (não dirigida), <i>animitta</i> (sem sinais).
