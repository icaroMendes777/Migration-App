<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
assim como dentre os animais o leão, o rei dos animais, é considerado como o
líder, isto é, em relação à sua força, velocidade e coragem, do mesmo modo,
dentre os estados que conduzem à iluminação, a faculdade da sabedoria é
considerada como a líder, isto é, para alcançar a iluminação. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E quais,
bhikkhus, são os estados que conduzem à iluminação? A faculdade da convicção,
bhikkhus, é um estado que conduz à iluminação; leva à iluminação. A faculdade
da energia é um estado que conduz à iluminação; leva à iluminação. A faculdade
da atenção plena é um estado que conduz à iluminação; leva à iluminação. A
faculdade da concentração é um estado que conduz à iluminação; leva à
iluminação. A faculdade da sabedoria é um estado que conduz à iluminação; leva
à iluminação.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Assim
como, bhikkhus, dentre os animais o leão é considerado como o principal, do
mesmo modo, dentre os estados que conduzem à iluminação a faculdade da
sabedoria é considerada como a principal, isto é, para alcançar a iluminação.”<o:p></o:p></span></p>


