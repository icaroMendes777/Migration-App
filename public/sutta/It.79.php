<p class=Normal> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: "Bhikkhus, três coisas conduzem ao declínio de um bhikkhu que é um treinando. Quais são as três? Aqui, um bhikkhu que é um treinando desfruta das atividades, ele gosta das atividades, desfruta de se entregar às atividades. Ele desfruta das conversas inúteis, ele gosta das conversas inúteis, desfruta de se entregar às conversas inúteis. Ele desfruta do sono, ele gosta do sono, desfruta de se entregar ao sono. Essas são as três coisas conduzem ao declínio de um bhikkhu que é um treinando.</p>

<p class=Normal>"Bhikkhus, essas três coisas protegem um bhikkhu que é um treinando do declínio. Quais são as três? Aqui, um bhikkhu que é um treinando não desfruta das atividades, ele não gosta das atividades, não desfruta de se entregar às atividades. Ele não desfruta das conversas inúteis, ele não gosta das conversas inúteis, não desfruta de se entregar às conversas inúteis. Ele não desfruta do sono, não ele gosta do sono, não desfruta de se entregar ao sono. Essas são as três coisas que protegem um bhikkhu que é um treinando do declínio."</p>

<p class=Normal style='margin-left:.75in'> Um bhikkhu que desfruta das atividades, <br>
inquieto, que gosta das conversas inúteis e do sono, <br>
nunca será capaz de realizar <br>
 a suprema iluminação.</p>

<p class=Normal style='margin-left:.75in'> Assim, que ele limite as suas responsabilidades, <br>
abra mão da preguiça e da inquietação; <br>
assim será capaz de realizar <br>
 a suprema iluminação.</p>


