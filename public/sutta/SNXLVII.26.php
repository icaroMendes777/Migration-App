<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Amigo, é porque ele desenvolveu parcialmente os quatro fundamentos da
atenção plena que ele é um treinando. Quais quatro? Neste caso, amigos, um
bhikkhu permanece contemplando o corpo como um corpo, ardente, plenamente
consciente e com atenção plena, tendo colocado de lado a cobiça e o desprazer
pelo mundo. Ele permanece contemplando as sensações como sensações ... mente
como mente ... objetos mentais como objetos mentais, ardente, plenamente
consciente e com atenção plena, tendo colocado de lado a cobiça e o desprazer
pelo mundo. Porque ele desenvolveu parcialmente os quatro fundamentos da
atenção plena é que ele é um treinando.”<o:p></o:p></span></p>

