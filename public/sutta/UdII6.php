<p class=Normal>Assim ouvi. Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no Parque de Anathapindika. </p>



<p class=Normal>Agora, naquela ocasião, a jovem esposa de um asceta errante se encontrava grávida, prestes a dar à luz. Então ela disse para o asceta errante, "Vá, brâmane, consiga algum óleo para o meu parto.”</p>



<p class=Normal>E quando isto foi dito, o asceta errante disse para a mulher "Mas onde poderei eu conseguir algum óleo?”</p>



<p class=Normal>Então, uma segunda vez, ela disse para o asceta errante, "Vá, brâmane, consiga algum óleo para o meu parto.”</p>



<p class=Normal>Quando isto foi dito, uma segunda vez, o asceta errante disse para a mulher "Mas onde poderei eu conseguir algum óleo?”</p>



<p class=Normal>Então, uma terceira vez, ela disse para o asceta errante, "Vá, brâmane, consiga algum óleo para o meu parto.”</p>



<p class=Normal>Quando isto foi dito, uma terceira vez, o asceta errante disse para a mulher "Mas onde poderei eu conseguir algum óleo?”</p>



<p class=Normal>Agora, naquela ocasião o armazém público do rei Pasenadi de Kosala estava distribuindo para contemplativos e brâmanes tanto óleo ou manteiga líquida quanto eles precisassem ingerir, mas não para que fossem levados dali. Então, o pensamento ocorreu ao asceta errante, "O armazém público do rei Pasenadi de Kosala está distribuindo para contemplativos e brâmanes tanto óleo ou manteiga líquida quanto eles precisem ingerir, mas não para que sejam levados dali. Suponha que, tendo ido até lá, eu beba tanto óleo quanto queira e, ao retornar, vomite este óleo e o use para o parto?”</p>



<p class=Normal>Então, tendo ido até o armazém público do rei Pasenadi de Kosala, ele bebeu tanto óleo quanto ele quis mas, ao voltar para casa, ele foi incapaz de expelir o óleo. Ele rolou para cima e para baixo no chão, sofrendo de dores violentas, afiadas e severas. Então, bem cedo pela manhã o Abençoado ajustou seu manto interno e – carregando a sua tigela e mantos – foi para Savatthi esmolar alimentos. Ele viu o asceta errante rolando para cima e para baixo no chão, sofrendo de dores violentas, afiadas e severas.</p>



<p class=Normal>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p>



	<p class=Normal style='margin-left:.75in'>"Que felicidade para aquele que nada possui.<br>

	Sábios são aqueles que nada possuem.<br>

	Veja o sofrimento de alguém possuidor de algo,<br>

	atado e agrilhoado a outrém.”</p>


