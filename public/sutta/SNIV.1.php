<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim ouvi.
Em certa ocasião, o Abençoado estava em Uruvela às margens do rio Neranjara ao
pé da figueira-dos-pagodes, pouco tempo depois de ter alcançado a perfeita
iluminação. [<a href="#N1">1</a>] <a name=R1></a>Então, enquanto o Abençoado
estava só e isolado, um pensamento surgiu na sua mente: “Eu de fato estou livre
daquele ascetismo penoso! É bom de fato que eu esteja livre daquele inútil
ascetismo penoso! É bom que, determinado e com atenção plena eu tenha alcançado
a iluminação!” [<a href="#N2">2</a><span class=GramE>]</span><o:p></o:p></span></p>



<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Então Mara, o Senhor do Mal tomando conhecimento com a sua mente do
pensamento na mente do Abençoado, foi até o Abençoado e se dirigiu a ele em versos:
<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span class=GramE><span lang=PT-BR
style='mso-ansi-language:PT-BR'>“Tendo se desviado das práticas austeras</span></span><span
lang=PT-BR style='mso-ansi-language:PT-BR'> <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>através das quais os homens se purificam,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>tornando-se impuro, você crê ser puro:<br>

<span class=GramE><span lang=PT-BR
style='mso-ansi-language:PT-BR'>você se desviou do caminho da purificação.”</span></span><span
lang=PT-BR style='mso-ansi-language:PT-BR'><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, o
Abençoado compreendendo, “Este é Mara, o Senhor do Mal,” respondeu em forma de
versos:<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span class=GramE><span lang=PT-BR
style='mso-ansi-language:PT-BR'>“Depois de compreender a inutilidade de toda
austeridade</span></span><span lang=PT-BR style='mso-ansi-language:PT-BR'><br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>na busca pelo imortal, [<a href="#N3">3</a><span class=GramE>]</span><br>

<a name=R3></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>e que todas essas penitências são fúteis<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>como remos e leme em terra firme. [<a href="#N4">4</a><span class=GramE>]</span><o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><a name=R4></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>Através do desenvolvimento do caminho para a
iluminação - <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>virtude, concentração e sabedoria <span class=GramE>-</span><br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>eu alcancei a pureza suprema:<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>você está derrotado, Senhor da Morte!<span class=GramE>” </span>[<a
