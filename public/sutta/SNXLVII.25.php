<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim ouvi.
Em certa ocasião, o Abençoado estava<span style='mso-spacerun:yes'>  </span>em
Savatthi no Bosque de Jeta, no Parque de Anathapindika. Então, um certo brâmane
foi até o Abençoado e ambos se cumprimentaram. Quando a conversa cortês e
amigável havia terminado ele sentou a um lado e disse:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mestre
Gotama, qual é a causa e razão porque o verdadeiro Dhamma não perdura depois
que um Tathagata tenha realizado o parinibbana? E qual é a causa e razão
porque o verdadeiro Dhamma perdura depois que um Tathagata tenha realizado o
parinibbana?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Brâmane, é
porque esses quatro fundamentos da atenção plena não são desenvolvidos e
cultivados que o verdadeiro Dhamma não perdura depois que um Tathagata tenha
realizado o parinibbana. E é porque esses quatro fundamentos da atenção plena
são desenvolvidos e cultivados que o verdadeiro Dhamma perdura depois que um
Tathagata tenha realizado o parinibbana. Quais quatro? Neste caso, amigo, um
bhikkhu permanece contemplando o corpo como um corpo, ardente, plenamente
consciente e com atenção plena, tendo colocado de lado a cobiça e o desprazer
pelo mundo. Ele permanece contemplando as sensações como sensações ... mente
como mente ... objetos mentais como objetos mentais, ardente, plenamente
consciente e com atenção plena, tendo colocado de lado a cobiça e o desprazer
pelo mundo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isso
foi dito, aquele brâmane disse para o Abençoado: “Magnífico, Mestre Gotama! Magnífico,
Mestre Gotama! Mestre Gotama esclareceu o Dhamma de várias formas, como se
tivesse colocado em pé o que estava de cabeça para baixo, revelasse o que
estava escondido, mostrasse o caminho para alguém que estivesse perdido ou
segurasse uma lâmpada no escuro para aqueles que possuem visão pudessem ver as
formas. Nós buscamos refúgio no Mestre Gotama, no Dhamma e na Sangha dos
bhikkhus. Que o Mestre Gotama me aceite como o discípulo leigo que buscou refúgio para o resto da vida.”<o:p></o:p></span></p>

