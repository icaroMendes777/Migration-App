<p class=Normal> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: "Para aquele que compreende e vê, bhikkhus, eu digo, ocorre a destruição das impurezas, não para aquele que não compreende e não vê. Mas para aquele que compreende o que, vendo o que, ocorre a destruição das impurezas? Para aquele que compreende e vê, 'Isto é sofrimento,' ocorre a destruição das impurezas. Para aquele que compreende e vê, 'Esta é a origem do sofrimento' ... 'Esta é a cessação do sofrimento' ... 'Este é o caminho que conduz à cessação do sofrimento,' ocorre a destruição das impurezas. Assim é, bhikkhus, que para aquele que compreende e vê ocorre a destruição das impurezas."</p>

<p class=Normal style='margin-left:.75in'> Para um treinando que pratica<br>
em conformidade com o caminho direto, <br>
o conhecimento da destruição surge primeiro<br>
seguido de imediato pelo conhecimento último.</p>

<p class=Normal style='margin-left:.75in'> Para aquele libertado através desse conhecimento último, <br>
o superior conhecimento da libertação, <br>
surge o conhecimento da destruição; <br>
"Assim os grilhões foram destruídos."</p>

<p class=Normal style='margin-left:.75in'> Certamente não pelo preguiçoso<br>
nem pelo tolo que não compreende, <br>
será nibbana realizado, <br>
o desatamento de todas amarras mundanas.</p>



