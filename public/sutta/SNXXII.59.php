<p class=Normal>
Assim ouvi. Em certa ocasião, o Abençoado estava em Benares, no Parque do Gamo, em Isipatana. Lá ele
se dirigiu ao grupo de cinco bhikkhus desta forma: “Bhikkhus” – “Venerável
Senhor,” eles responderam. O Abençoado disse o seguinte: <o:p></o:p></span></p>



<p class=Normal>
“A forma, bhikkhus, é não-eu. Pois, bhikkhus, se a forma fosse o eu,
essa forma não conduziria ao sofrimento e seria possível obter da forma: ‘Que a
minha forma seja assim; que a minha forma não seja assim.’ Mas porque a forma é
não-eu, a forma conduz ao sofrimento e não é possível obter da forma: ‘Que a
minha forma seja assim; que a minha forma não seja assim.’<span
style='mso-spacerun:yes'>  </span><o:p></o:p></span></p>



<p class=Normal>
“A sensação é não-eu... <o:p></o:p></span></p>



<p class=Normal>
“A percepção<span style='mso-spacerun:yes'>  </span>é não-eu... <o:p></o:p></span></p>



<p class=Normal>
“As formações volitivas são não-eu... <o:p></o:p></span></p>



<p class=Normal>
“A consciência é não-eu. Pois, bhikkhus, se a consciência fosse o eu,
essa consciência não conduziria ao sofrimento e seria possível obter da consciência:
‘Que a minha consciência seja assim; que a minha consciência não seja assim.’
Mas porque a consciência é não-eu, a consciência conduz ao sofrimento e não é
possível obter da consciência: ‘Que a minha consciência seja assim; que a minha
consciência não seja assim.’<span style='mso-spacerun:yes'>  </span><o:p></o:p></span></p>



<p class=Normal>
“O que vocês pensam, bhikkhus, a forma é permanente ou impermanente?<o:p></o:p></span></p>



<p class=Normal>
“Impermanente, senhor. <o:p></o:p></span></p>



<p class=Normal>
&quot;E aquilo que é impermanente é sofrimento ou felicidade? <o:p></o:p></span></p>



<p class=Normal>
“Sofrimento, senhor.<o:p></o:p></span></p>



<p class=Normal>
“E é adequado considerar o que é impermanente, sofrimento, sujeito a
mudanças como: ‘Isso é meu. Isso sou eu. Isso é o meu eu’? <o:p></o:p></span></p>



<p class=Normal>
“Não, senhor.<o:p></o:p></span></p>



<p class=Normal>
“... a sensação é permanente ou impermanente?<o:p></o:p></span></p>



<p class=Normal>
“Impermanente, senhor.<o:p></o:p></span></p>



<p class=Normal>
“... a percepção é permanente ou impermanente?<o:p></o:p></span></p>



<p class=Normal>
“Impermanente, senhor.<o:p></o:p></span></p>



<p class=Normal>
“... as formações volitivas são permanentes ou impermanentes?<o:p></o:p></span></p>



<p class=Normal>
“Impermanentes, senhor. <o:p></o:p></span></p>



<p class=Normal>
“O que vocês pensam, bhikkhus, a consciência é permanente ou
impermanente? <o:p></o:p></span></p>



<p class=Normal>
“Impermanente, senhor. <o:p></o:p></span></p>



<p class=Normal>
“E aquilo que é impermanente é sofrimento ou felicidade?<o:p></o:p></span></p>



<p class=Normal>
“Sofrimento, senhor.<o:p></o:p></span></p>



<p class=Normal>
“E é adequado considerar o que é impermanente, sofrimento, sujeito a
mudanças como: 'Isso é meu. Isso sou eu. Isso é o meu eu’?<span
style='mso-spacerun:yes'>  </span><o:p></o:p></span></p>



<p class=Normal>
“Não, senhor. <o:p></o:p></span></p>



<p class=Normal>
“Portanto, bhikkhus, qualquer forma, quer seja do passado, futuro ou
presente, interna ou externa; grosseira ou sutil; inferior ou superior, próxima
ou distante: toda forma deve ser vista como na verdade é, com correta
sabedoria: ‘Isso não é meu, isso não sou eu, isso não é o meu eu.’<o:p></o:p></span></p>



<p class=Normal>
“Qualquer sensação ... <o:p></o:p></span></p>



<p class=Normal>
“Qualquer percepção ... <o:p></o:p></span></p>



<p class=Normal>
“Quaisquer formações volitivas ... <o:p></o:p></span></p>



<p class=Normal>
“Qualquer consciência, quer seja do passado, do futuro ou do presente,
interna ou externa; grosseira ou sutil; inferior ou superior; próxima ou
distante: toda consciência deve ser vista como na verdade é, com correta
sabedoria: ‘Isso não é meu, isso não sou eu, isso não é o meu eu.’<o:p></o:p></span></p>



<p class=Normal>
“Vendo dessa forma, o nobre discípulo bem instruído se desencanta com a
forma, se desencanta com a sensação, se desencanta com a percepção, se
desencanta com as formações volitivas, se desencanta com a consciência. Desencantado ele
se torna desapegado.  Através
do desapego a sua mente é libertada. Quando ela está libertada surge o
conhecimento: ‘Libertada.’ Ele compreende que: ‘O nascimento foi destruído, a
vida santa foi vivida, o que deveria ser feito foi feito, não há mais vir a ser
a nenhum estado.’”</span>
 <o:p></o:p></span></p>



<p class=Normal>
Isso foi o que o Abençoado disse.  Os bhikkhus ficaram satisfeitos e contentes com as
palavras do Abençoado. E enquanto essa explanação estava sendo dada, as
mentes do grupo de cinco bhikkhus, foram libertadas das impurezas através do
desapego. <o:p></o:p></span></p>

