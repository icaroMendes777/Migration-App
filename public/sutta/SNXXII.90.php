<p class=Normal><b style='mso-bidi-font-weight:normal'><span lang=PT-BR
style='mso-ansi-language:PT-BR'>Nota de Thanissaro Bhikkhu: </span></b><span
lang=PT-BR style='mso-ansi-language:PT-BR'>Trechos do Vinaya mostram que o venerável
Channa, ao que parece, o cavaleiro do Príncipe Siddhattha na noite da sua
Grande Renúncia, era orgulhoso e teimoso. Depois de se tornar um bhikkhu, ele
não se dispunha a aceitar instruções de nenhum outro bhikkhu. O <a
href="DN16.php#T6_4">DN 16.6.4</a> relata que o Buda, na noite do seu
parinibbana, impôs a ele a punição de Brahma: ele deveria ser deixado por sua
própria conta sem que ninguém o ensinasse ou corrigisse. De acordo com o Cv.XI
ao receber a notícia dessa punição o venerável Channa ficou tão chocado que
desmaiou. Depois disso ele praticou em isolamento, diligente, até alcançar o
estado de arahant. O venerável Ananda, mais tarde, disse que a sua realização
anulava a punição. Este sutta conta uma história diferente do arrependimento do
venerável Channa.<o:p></o:p></span></p>


