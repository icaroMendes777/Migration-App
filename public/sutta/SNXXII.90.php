<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em certa
ocasião, muitos bhikkhus sêniores estavam próximos a Benares no Parque
do Gamo, em Isipatana. Então, no final da tarde o venerável Channa saiu do seu
isolamento e tomando a sua chave, foi de habitação em habitação dizendo aos
bhikkhus sêniores, “Que o venerável sênior me encoraje, que o venerável sênior
me ensine, que o venerável sênior faça um discurso do Dhamma de modo que eu
possa ver o Dhamma.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isso
foi dito, os bhikkhus sêniores disseram para o venerável Channa: “A forma, amigo
Channa, é impermanente. A sensação é impermanente. A percepção é impermanente.
As formações volitivas são impermanentes. A consciência é impermanente. A forma é não-eu.
A sensação é não-eu. A percepção é não-eu. As formações volitivas são não-eu. A
consciência é não-eu. Todas formações volitivas são impermanentes. Todos fenômenos são
não-eu.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, o
pensamento ocorreu ao venerável Channa: “Eu, também, penso que a forma é
impermanente, a sensação é impermanente, a percepção é impermanente, as
formações volitivas são impermanentes, a consciência é impermanente; a forma é não-eu, a
sensação é não-eu, a percepção é não-eu, as formações volitivas são não-eu, a consciência
é não-eu; todas as formações volitivas são impermanentes; todos os fenômenos são não-eu.
Mas, ainda assim a minha mente não salta, ganha confiança, firmeza e libertação
com o silenciar de todas as formações, o abandono de todas as aquisições, a
destruição do desejo, desapego, cessação, nibbana. Ao invés disso, a agitação e
o apego surgem e a minha mente se retrai, pensando, ‘Mas quem, então, é o meu
eu?’ No entanto, esse pensamento não ocorre naquele que vê o Dhamma. Portanto,
quem me poderia ensinar o Dhamma de forma que eu pudesse ver o Dhamma?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, o
pensamento ocorreu ao venerável Channa: “Esse venerável Ananda está em Kosambi no Parque
de Ghosita. Ele foi elogiado pelo Mestre e é estimado pelos seus sábios
companheiros na vida santa. Ele seria capaz de ensinar o Dhamma de<span
style='mso-spacerun:yes'>  </span>modo que eu pudesse ver o Dhamma, tenho nele
repentina confiança. Porque não procuro pelo venerável Ananda?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim,
arrumando a sua habitação e carregando os seus mantos e tigela, o venerável Channa
foi até Kosambi onde o venerável Ananda estava no Parque de Ghosita. Ao chegar,
ambos se cumprimentaram. Quando a conversa cortês e amigável havia terminado,
ele sentou a um lado e relatou ao venerável Ananda o que havia acontecido e
acrescentou: “Que o venerável Ananda possa me encorajar, que o venerável Ananda possa me
ensinar, que o venerável Ananda possa fazer um discurso do Dhamma para que eu possa
ver o Dhamma.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Só com
isso estou satisfeito com o venerável Channa, por ele se abrir e deixar de lado a
sua teimosia. Portanto, ouça bem, amigo Channa. Você é capaz de compreender o
Dhamma.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, uma
repentina alegria e êxtase brotaram no venerável Channa com o pensamento, “Então, eu
sou capaz de compreender o Dhamma!”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Na
presença do Abençoado eu ouvi isso, amigo Channa. Na presença do Abençoado
aprendi a exortação que ele proferiu para o bhikkhu Kaccayanagotta:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Em geral,
Kaccayana, este mundo depende de uma dualidade, a noção da existência e a noção
da não existência ... [<a href="SNXII.15.php">SN XII.15</a>] ... Essa é a
cessação de toda essa massa de sofrimento.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Assim é
como são as coisas, amigo Ananda, para aqueles que possuem amigos na vida santa
como o venerável Ananda – compassivo, que ajuda, encoraja e ensina. Precisamente
agora, ouvindo o ensinamento do Dhamma do <a name=PVW>Ven</a>. Ananda, eu
consegui penetrar o Dhamma .” <o:p></o:p></span></p>

