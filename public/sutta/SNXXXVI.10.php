<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
essas três sensações nascem do contato, estão enraizadas no contato, têm o contato
como sua fonte e condição. Quais três? Sensações prazerosas, sensações
dolorosas, sensações nem dolorosas, nem prazerosas.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Na dependência de um
contato para ser experimentado como prazeroso, bhikkhus, surge uma sensação
prazerosa. Com a cessação daquele contato para ser experimentado como
prazeroso, a correspondente sensação - a sensação prazerosa que surgiu na
dependência daquele contato para ser experimentado como prazeroso – também
cessa e é aplacada. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Na dependência de um
contato para ser experimentado como doloroso, bhikkhus, surge uma sensação
dolorosa. Com a cessação daquele contato para ser experimentado como doloroso,
a correspondente sensação - a sensação dolorosa que surgiu na dependência
daquele contato para ser experimentado como doloroso – também cessa e é
aplacada.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Na dependência de um
contato para ser experimentado como nem doloroso, nem prazeroso, bhikkhus,
surge uma sensação nem dolorosa, nem prazerosa. Com a cessação daquele contato
para ser experimentado como nem doloroso, nem prazeroso, a correspondente
sensação - a sensação nem dolorosa, nem prazerosa que surgiu na dependência
daquele contato para ser experimentado como nem doloroso, nem prazeroso –
também cessa e é aplacada.<span style="mso-spacerun: yes">   </span><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus, assim como o
calor e o fogo são produzidos pela junção e fricção de dois gravetos de
madeira, mas quando os gravetos são separados e postos de lado o calor
resultante cessa e é aplacado, da mesma maneira aquelas três sensações nascem
do contato, estão enraizadas no contato, têm o contato como sua fonte e
condição. Na dependência do contato apropriado, a sensação correspondente
surge; com a cessação do contato apropriado, a sensação correspondente cessa.”<o:p></o:p></span></p>


