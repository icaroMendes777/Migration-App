<p class=Normal> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: “Bhikkhus, há essas três sensações. Quais três? Sensações prazerosas, sensações desprazerosas, sensações nem prazerosas nem desprazerosas. A sensação prazerosa, bhikkhus, deve ser vista como sofrimento [<a href="#N1">1</a>], a sensação desprazerosa deve ser vista como uma flecha, a sensação nem prazerosa nem desprazerosa deve ser vista como impermanente.

<p class=Normal> "Quando um bhikkhu vê essas sensações assim, é dito que ele é um nobre que vê corretamente. Ele cortou o desejo, rompeu os grilhões, e penetrando completamente a presunção deu um fim ao sofrimento."</p>

<p class=Normal style='margin-left:.75in'> Ele vê o prazer como sofrimento <br>
a dor como uma flecha. <br>
Vê como impermanente toda sensação pacífica<br>
que não é nem prazerosa nem dolorosa.</p>

<p class=Normal style='margin-left:.75in'> Um bhikkhu que assim vê corretamente 
está bem libertado. <br>
Com o conhecimento realizado, em paz, <br>
esse sábio superou todos os apegos.</p>


