<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião o Abençoado estava em Rajagaha, no Bambual, no Santuário
dos Esquilos. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. Nessa época o ven.
Rahula estava em Ambalatthika. [<a href="#N1">1</a>]<span
style='mso-spacerun:yes'>  </span><a name=R1></a>Então, quando já era noite, o
Abençoado levantou-se da meditação e foi até onde o ven. Rahula estava em
Ambalatthika. O ven. Rahula o viu chegando à distância,<span
style='mso-spacerun:yes'>  </span>preparou um assento e água para lavar os pés.
O Abençoado sentou no assento preparado e<span style='mso-spacerun:yes'> 
</span>lavou os pés. O ven. Rahula homenageou o Abençoado e sentou a um lado. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. Então, o Abençoado,
tendo deixado um pouco de água na jarra, disse para o ven. Rahula,
&quot;Rahula, você vê este pouco de água nesta jarra?&quot; - &quot;Sim,
senhor.&quot; - &quot;Assim mesmo, Rahula, esse é o pouco da qualidade de
contemplativo que resta naquele que não se envergonha de com plena consciência
contar uma mentira.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. Tendo posto fora a
pequena quantidade de água que restava, o Abençoado disse para o ven. Rahula,
&quot;Rahula, você vê como essa pequena quantidade de água foi posta
fora?&quot; - &quot;Sim, senhor.&quot; - &quot;Assim mesmo, Rahula, aqueles que
não se envergonham de com plena consciência contar uma mentira, jogam fora a
sua qualidade de contemplativo.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. Tendo virado a jarra de
cabeça para baixo, o Abençoado disse para o ven. Rahula, &quot;Rahula, você vê
como essa jarra foi virada de cabeça para baixo?&quot; - &quot;Sim,
senhor.&quot; - &quot;Assim mesmo, Rahula, aqueles que não se envergonham de com
plena consciência contar uma mentira, colocam a sua qualidade de contemplativos
virada de cabeça para baixo<span style='mso-spacerun:yes'>   </span><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. Tendo virado a jarra de
água para cima, o Abençoado disse para o ven. Rahula, &quot;Rahula, você vê
como a jarra está vazia e oca?&quot; - &quot;Sim, senhor.&quot; - &quot;Assim
mesmo, Rahula, é vazia e oca a qualidade de contemplativo daquele que não se
envergonha de com plena consciência contar uma mentira. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. &quot;Suponha, Rahula,
um elefante real: imenso, com pedigree, acostumado a batalhas, suas presas como
traves de uma carruagem. Em uma batalha, ele usa as patas dianteiras e
traseiras, os quartos dianteiros e traseiros, sua cabeça e orelhas, suas presas
e rabo, porém ele guarda a sua tromba. O treinador de elefantes nota isso e
pensa, 'Esse elefante real: imenso ... guarda a sua tromba. Ele não deu a sua
vida pelo rei.' Porém quando o elefante real ... em uma batalha, usa as patas
dianteiras e traseiras, os quartos dianteiros e traseiros, sua cabeça e
orelhas, suas presas e rabo e a sua tromba, o treinador de elefantes nota isso
e pensa, 'Esse elefante real: imenso ... e a sua tromba. Ele deu a sua vida pelo
rei. Não há nada que ele não faça.' O mesmo é verdadeiro com qualquer um que
não se envergonhe de com plena consciência contar uma mentira: não há mal, eu
digo, que ele não possa cometer. Rahula, você deve treinar, 'Eu não direi uma
mentira deliberada mesmo por brincadeira.' <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. &quot;O que você pensa,
Rahula? Para que serve um espelho?&quot; &quot;Para refletir, venerável
senhor.&quot; &quot;Da mesma forma, Rahula, ações corporais devem ser feitas
após repetida reflexão, ações verbais devem ser feitas após repetida reflexão e
ações mentais devem ser feitas após repetida reflexão. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. “Rahula, quando você
quiser praticar uma ação corporal, você deveria refletir a respeito: 'Esta ação
corporal que quero praticar - conduzirá à minha própria aflição, à aflição de
outros, ou ambos? É uma ação corporal sem habilidade, com conseqüências
dolorosas, resultados dolorosos?' Se, refletindo, você sabe que conduzirá à sua
própria aflição, à aflição de outros, ou ambos; será uma ação sem habilidade
com conseqüências dolorosas, resultados dolorosos, então qualquer ação corporal
desse tipo é totalmente inadequada. Porém se refletindo, você sabe que não
causará aflição ... será uma ação habilidosa com felizes conseqüências, felizes
resultados, então qualquer ação corporal desse tipo é adequada.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. &quot;Também, Rahula,
enquanto você estiver praticando uma ação corporal, você deveria refletir a seu
respeito: 'Esta ação corporal que estou praticando - conduzirá à minha própria
aflição, à aflição de outros, ou ambos? É uma ação corporal sem habilidade, com
conseqüências dolorosas, resultados dolorosos?' Se, refletindo, você sabe que
conduzirá à sua própria aflição, à aflição de outros, ou ambos ... você deveria
desistir dela. Porém se refletindo você sabe que não é ... você pode continuar
com a ação corporal.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. “Também, Rahula, tendo
praticado uma ação corporal, você deveria refletir a respeito ... se,
refletindo, você sabe que conduziu à sua própria aflição, à aflição de outros,
ou ambos; foi uma ação sem habilidade com conseqüências dolorosas, resultados
dolorosos, então você deveria confessá-la, revelá-la, abri-la para o Mestre ou
um sábio companheiro na vida santa. Tendo confessado ... você deve exercer
contenção no futuro.[<a href="#N2">2</a>] <a name=R2></a>Porém se refletindo
você sabe que não conduziu à aflição ... foi um ação corporal habilidosa com
conseqüências felizes, resultados felizes, então você deveria se sentir
mentalmente renovado e contente, treinando dia e noite nos estados benéficos.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. &quot;Rahula, quando
você quiser praticar uma ação verbal ... (complete com o par. 9 substituindo
“corporal” por “verbal”) ... então qualquer ato verbal desse tipo é adequado. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. &quot;Também, Rahula,
enquanto você estiver praticando uma ação verbal .... (complete com o par. 10
substituindo “corporal” por “verbal”) ... você pode continuar com a ação
verbal.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14. “Também, Rahula, tendo
praticado uma ação verbal ... (complete com o par. 11 substituindo “corporal”
por “verbal”) ... então você deveria se sentir mentalmente renovado e contente,
treinando dia e noite nos estados benéficos. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. &quot;Rahula, quando
você quiser praticar uma ação mental .... (complete com o par. 9 substituindo
“corporal” por “mental”) ... então qualquer ato mental desse tipo é adequado. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16. &quot;Também, Rahula,
enquanto você estiver praticando uma ação mental .... (complete com o par. 10
substituindo “corporal” por “mental”) ... você pode continuar com a ação
mental.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17. “Também, Rahula, tendo
praticado uma ação mental ... (complete com o par. 11 substituindo “corporal”
por “mental” [<a href="#N3">3</a>]) ... <a name=R3></a>então você deveria se
sentir mentalmente renovado e contente, treinando dia e noite nos estados
benéficos.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18. &quot;Rahula, todos os
brâmanes e contemplativos que no passado purificaram as suas ações corporais,
ações verbais e ações mentais, o fizeram através da repetida reflexão dessa
mesma forma. Todos os brâmanes e contemplativos que no futuro purificarão as
suas ações corporais, ações verbais e ações mentais, o farão através da
repetida reflexão dessa mesma forma. Todos os brâmanes e contemplativos que
no presente purificam as suas ações corporais, ações verbais e ações mentais, o
fazem através da repetida reflexão dessa mesma forma. Portanto, Rahula, você
deve treinar dessa forma: 'Eu purificarei minhas ações corporais, ações verbais
e ações mentais através da repetida reflexão.'&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o que o Abençoado
disse. O ven. Rahula ficou satisfeito e contente com as palavras do Abençoado. </span><span
lang=PT-BR style='font-size:10.0pt;mso-ansi-language:PT-BR'><o:p></o:p></span></p>

