<p class=Normal> “Bhikkhus, apliquem nas formas a atenção com sabedoria. Reconheçam a impermanência das formas tal como ela na verdade é. Quando um bhikkhu aplica nas formas a atenção com sabedoria e reconhece a impermanência tal como ela na verdade é, ele experimenta o desencantamento. Com a destruição do prazer ocorre a destruição da cobiça; com a destruição da cobiça ocorre a destruição do prazer. Com a destruição do prazer e cobiça a mente é libertada e diz- se que ela está bem libertada.</p>

