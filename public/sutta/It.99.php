<p class=Normal><a name=R1></a> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: "Bhikkhus, eu declaro que é através do Dhamma que alguém se torna um brâmane possuindo conhecimento tríplice: eu não digo isso de alguém simplesmente porque ele sabe falar e recitar de modo persuasivo. [<a href="#N1">1</a>] E como eu declaro que é através do Dhamma que alguém se torna um brâmane possuindo conhecimento tríplice?</p>

<p class=Normal> "Aqui, bhikkhus, um bhikkhu se recorda das suas muitas vidas passadas, isto é, um nascimento, dois nascimentos, três nascimentos, quatro, cinco, dez, vinte, trinta, quarenta, cinqüenta, cem, mil, cem mil, muitos ciclos cósmicos de contração, muitos ciclos cósmicos de expansão, muitos ciclos cósmicos de contração e expansão, ‘Lá eu tinha tal nome, pertencia a tal clã, tinha tal aparência. Assim era o meu alimento, assim era a minha experiência de prazer e dor, assim foi o fim da minha vida. Falecendo daquele estado, eu ressurgi ali. Ali eu também tinha tal nome, pertencia a tal clã, tinha tal aparência. Assim era o meu alimento, assim era a minha experiência de prazer e dor, assim foi o fim da minha vida. Falecendo daquele estado, eu ressurgi aqui.’ Assim ele se recorda das suas muitas vidas passadas nos seus modos e detalhes. Esse é o primeiro conhecimento que ele obtém. A ignorância é destruída, surge a luz, como acontece com quem permanece ardente, diligente e decidido.</p>

<p class=Normal> "Depois novamente, bhikkhus, por meio do olho divino, que é purificado e ultrapassa o humano, ele vê seres falecendo e renascendo, inferiores e superiores, bonitos e feios, afortunados e desafortunados. Ele compreende como os seres prosseguem de acordo com as suas ações desta forma: ‘Esses seres – dotados de má conduta com o corpo, linguagem e mente, que insultam os nobres, com o entendimento incorreto e realizando ações sob a influência do entendimento incorreto – com a dissolução do corpo, após a morte, renasceram num estado de privação, num destino infeliz, nos reinos inferiores, até mesmo no inferno. Porém estes seres - dotados de boa conduta com o corpo, linguagem e mente, que não insultam os nobres, com o entendimento correto e realizando ações sob a influência do entendimento correto – com a dissolução do corpo, após a morte, renasceram num destino feliz, no paraíso.’ Dessa forma - por meio do olho divino, que é purificado e ultrapassa o humano, ele vê seres falecendo e renascendo, inferiores e superiores, bonitos e feios, afortunados e desafortunados, e ele compreende como os seres prosseguem de acordo com as suas ações. Esse é o segundo conhecimento que ele obtém. A ignorância é destruída, surge a luz, como acontece com quem permanece ardente, diligente e decidido.</p>

<p class=Normal> "Depois novamente, bhikkhus, através do conhecimento direto, um bhikkhu aqui e agora entra e permanece na libertação da mente e libertação através da sabedoria que são imaculadas com a destruição de todas as impurezas. Esse é o terceiro conhecimento que ele obtém. A ignorância é destruída, surge a luz, como acontece com quem permanece ardente, diligente e decidido.</p>

<p class=Normal> "Portanto, bhikkhus, eu declaro que é através do Dhamma que alguém se torna um brâmane possuindo conhecimento tríplice: eu não digo isso de alguém simplesmente porque ele sabe falar e recitar de modo persuasivo."</p>

<p class=Normal style='margin-left:.75in'> Aquele que conhece suas vidas passadas, <br>
que vê o paraíso e os estados miseráveis, <br>
que realiza o fim do nascimento, <br>
um sábio e mestre do conhecimento direto -</p>

<p class=Normal style='margin-left:.75in'>Através desses três tipos de conhecimento ele se torna<br>
um brâmane com o conhecimento tríplice. <br>
Isso é aquilo que chamo o conhecimento tríplice, <br>
não alguém tagarelando e recitando.</p>



