<p class=Normal>Assim ouvi. Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no Parque de Anathapindika.</p>



<p class=Normal>Agora, naquela ocasião um grande grupo de bhikkhus vinha em direção ao Abençoado, com Bhaddiya, o anão, vindo por último, atrás dos muitos bhikkhus.</p>



<p class=Normal>O Abençoado viu o venerável Bhaddiya, o anão, mesmo que de longe, vindo por último, atrás dos muitos bhikkhus: com aspecto ruim, má aparência, um anão, tendo uma forma desprezada pela maioria daqueles bhikkhus. Tendo visto ele, o Abençoado se dirigiu aos bhikkhus.</p>



<p class=Normal>"Bhikkhus, vocês vêem, mesmo que de longe, aquele monge vindo por último, atrás de muitos bhikkhus: com aspecto ruim, má aparência, um anão, tendo uma forma desprezada pela maioria daqueles bhikkhus?”</p>



<p class=Normal>"Sim, venerável senhor.”</p>



<p class=Normal>"Bhikkhus, este bhikkhu é alguém de grande poder, grande força. A realização já alcançada por ele não é fácil de se alcançar. E é através disto que ele alcançou e permaneceu no objetivo supremo da vida santa pelo qual membros de um clã deixam a vida em família pela vida santa, tendo conhecido e realizado por si mesmo no aqui e agora.”</p>



<p class=Normal>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p>



	<p class=Normal style='margin-left:.75in'>"A carruagem avança perfeita,<br>

	com seu toldo branco e único eixo.<br>

	Veja ele se aproximar, tranquilo,<br>

	com as amarras destruídas, libertado.”</p>

