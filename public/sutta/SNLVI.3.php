cessação do sofrimento. Quaisquer membros de clãs que no passado de modo
correto deixaram a vida em família ... deixarão a vida em família ... deixam a
vida em família pela vida santa, todos eles assim o fazem para penetrar as
Quatro Nobres Verdades como elas na verdade são.<o:p></o:p></span></p>


