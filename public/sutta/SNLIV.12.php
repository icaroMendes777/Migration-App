<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] Todos os treinandos, <i>(sekhas),</i> abandonaram por completo o
obstáculo da dúvida; além disso, aqueles que não retornam erradicaram a má
vontade e a ansiedade (bem como o desejo sensual num sentido mais específico).
Os treinandos abandonam os demais obstáculos apenas temporariamente através dos
jhanas. [<a href="#R1">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[2] Na descrição da prática da atenção plena na respiração do Buda, <i>sato
va </i>( “atenção plena <i>justa</i>”) é substituída por <i>sato, </i>e <i>sikkhati</i>(“ele treina”) é omitido por completo. <i>Va
</i>é omitido para mostrar a excepcional serenidade da sua prática, visto que
as inspirações e expirações sempre estão claras para ele; <i>sikkhati </i>é
omitido porque ele está além do treinamento. [<a href="#R2">Retorna</a>]<o:p></o:p></span></p>







