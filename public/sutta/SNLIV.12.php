<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Certa
ocasião, o Venerável Lomasavangisa estava entre os Sakyas em Kapilavatthu no
Parque de Nigrodha. Então, o Sakya Mahanama foi até o Venerável Lomasavangisa e
depois de cumprimentá-lo sentou a um lado e disse:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“É o caso,
venerável senhor, em que a permanência de um treinando é em si a mesma
permanência do Tathagata ou a permanência de um treinando é uma coisa e a
permanência do Tathagata é outra?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não é o
caso, amigo Mahanama, em que a permanência de um treinando é em si a mesma
permanência do Tathagata; a permanência de um treinando é uma coisa e a
permanência do Tathagata é outra.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigo
Mahanama, aqueles bhikkhus que são treinandos, que não alcançaram o ideal das
suas mentes, que permanecem aspirando pela insuperável segurança contra o cativeiro,
permanecem com os cinco obstáculos abandonados. [<a href="#N1">1</a>] <a
name=R1></a>Quais cinco? O obstáculo do desejo sensual, má vontade, preguiça e
torpor, inquietação e ansiedade, e dúvida. Aqueles bhikkhus que são treinandos
... permanecem com esses cinco obstáculos abandonados.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas, amigo
Mahanama, aqueles bhikkhus que são Arahants, com as impurezas destruídas, que
viveram a vida santa, fizeram o que devia ser feito, depuseram o fardo,
alcançaram o verdadeiro objetivo, destruíram os grilhões da existência e estão
completamente libertados através do conhecimento supremo, os cinco obstáculos
foram abandonados, cortados pela raiz, feitos como com um tronco de palmeira,
eliminados de tal forma que não estarão mais sujeitos a um futuro surgimento. Quais
cinco? O obstáculo do desejo sensual, má vontade, preguiça e torpor,
inquietação e ansiedade, e dúvida. Aqueles bhikkhus que são Arahants ... esses
cinco obstáculos foram abandonados, cortados pela raiz, feitos como com um
tronco de palmeira, eliminados de tal forma que não estarão mais sujeitos a um
futuro surgimento.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“ Através
do método que segue também, amigo Mahanama, pode ser compreendido como a
permanência de um treinando é uma coisa e a permanência do Tathagata é outra. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Certa
ocasião, amigo Mahanama, o Abençoado estava em Icchanangala na Floresta de
Icchanangala. Então, o Abençoado se dirigiu aos bhikkhus desta forma:
‘Bhikkhus, eu quero estar em retiro por três meses. Eu não devo ser abordado
por ninguém exceto aquele que me traz a comida esmolada.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
venerável senhor,” aqueles bhikkhus responderam e ninguém se aproximou do
Abençoado exceto aquele que lhe levou a comida esmolada. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então,
quando havia passado três meses, o Abençoado emergiu do seu isolamento e se
dirigiu aos bhikkhus desta forma:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
se errantes de outras seitas perguntarem a vocês: ‘Em que permanência, amigos,
em geral o Abençoado permanece durante o retiro das chuvas?’ – sendo assim
perguntados, vocês deveriam responder a esses errantes assim: ‘Durante o retiro
das chuvas, amigos, o Abençoado em geral permanece na concentração através da
atenção plena na respiração.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Nesse
caso, bhikkhus, eu inspiro com atenção plena, eu expiro com atenção plena.
Inspirando longo, eu compreendo: ‘eu inspiro longo’; ou expirando longo, eu
compreendo: ‘eu expiro longo.’ Inspirando curto, eu compreendo: ‘eu inspiro
curto’; ou expirando curto, eu compreendo: ‘eu expiro curto.’ Eu compreendo: ‘
eu inspiro experienciando todo o corpo’ ... Eu compreendo:<span
style="mso-spacerun: yes">  </span>‘eu expiro contemplando a renúncia.’[<a
href="#N2">2</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“’Falando corretamente, bhikkhus, se fosse para alguém dizer de algo: “É
uma permanência nobre, uma permanência divina, a permanência do Tathagata,” é
da concentração através da atenção plena na respiração que, falando o que é
certo, isso deveria ser dito.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Através
desse método, amigo Mahanama, pode ser compreendido como a permanência de um
treinando é uma coisa e a permanência do Tathagata é outra.”<o:p></o:p></span></p>




