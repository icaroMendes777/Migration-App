<p class=Normal>Então um certo brâmane foi até o Abençoado e depois da troca de saudações corteses e amáveis ele sentou a um lado e disse:</p>

<p class=Normal>"O que o Mestre Gotama afirma, o que ele declara?"</p>

<p class=Normal>"Brâmane, eu afirmo uma doutrina da ação e uma doutrina da não-ação."</p>

<p class=Normal>"Mas de que forma o Mestre Gotama afirma uma doutrina da ação e uma doutrina da não-ação?"</p>

<p class=Normal>"Eu afirmo a não-ação com relação à conduta corporal imprópria, conduta verbal imprópria e conduta mental imprópria; eu afirmo a não-ação com relação aos vários tipos de qualidades ruins e prejudiciais. [<a href="#N1">1</a>] Eu afirmo a ação com relação à conduta corporal apropriada, conduta verbal apropriada e conduta mental apropriada; eu afirmo a ação com relação aos vários tipos de qualidades benéficas. É dessa forma, brâmane, que eu afirmo uma doutrina da ação e uma doutrina da não-ação."</p>


