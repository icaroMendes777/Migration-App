<p class=Normal> “Venerável senhor, seria bom se o Abençoado me ensinasse o Dhamma de forma resumida, de modo que, tendo ouvido o Dhamma do Abençoado eu possa permanecer só, isolado, diligente, ardente e decidido.” </p>

