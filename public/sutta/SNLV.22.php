<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Na direção
que ela estivesse declivando, tendendo e se inclinado, venerável senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Da mesma
