Em certa ocasião o Abençoado estava<span style="mso-spacerun: yes"> 
</span>entre os Sakyas em Kapilavatthu, no Parque de Nigrodha. Então o Sakya
Mahanama foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e
disse:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, esta Kapilavatthu é rica, próspera e populosa, repleta de gente e bem
suprida. À noite, quando entro em Kapilavatthu, depois de visitar o Abençoado
ou os bhikkhus dignos de respeito, eu topo com um elefante desgarrado, um
cavalo desgarrado, uma carruagem desgarrada, uma carroça desgarrada, um homem
desgarrado. Nessa ocasião, venerável senhor, a minha atenção plena em relação
ao Abençoado fica confusa, a minha atenção plena em relação ao Dhamma fica
confusa, a minha atenção plena em relação à Sangha fica confusa. O pensamento
então me ocorre: ‘Se neste momento eu morresse, qual seria o meu destino, qual
seria o meu futuro percurso?’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não tema,
Mahanama! Não tema, Mahanama! A sua morte não será ruim, o seu falecimento não
será ruim. O nobre discípulo que possui quatro coisas decliva, tende e se
inclina na direção de Nibbana. Quais quatro? Aqui. Mahanama, o nobre discípulo
possui perfeita claridade, serenidade e confiança no Buda ... no Dhamma ... na Sangha ... possui as
virtudes apreciadas pelos nobres – intactas ... que conduzem à concentração.[<a
href="#N1">1</a>]<o:p></o:p></span></p>



