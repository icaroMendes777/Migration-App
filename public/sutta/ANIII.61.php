<p class=Normal><span lang=PT-BR>&nbsp; “Bhikkhus, existem esses
três tipos de associações sectárias que – quando questionadas, pressionadas em
dar suas razões e censuradas por pessoas sábias – embora possam dar uma
explicação contrária, permanecem presas a [uma doutrina de] inércia. Quais
três?<o:p></o:p></span></p>


<p class=Normal “Existem brâmanes e contemplativos que acreditam nesse ensinamento, que
pensam dessa maneira: ‘Qualquer que seja a experiência de uma pessoa –
prazerosa, dolorosa ou neutra – tudo tem sua causa no que foi feito no
passado.’ Existem brâmanes e contemplativos que acreditam nesse ensinamento,
que pensam dessa maneira: ‘Qualquer que seja a experiência de uma pessoa –
prazerosa, dolorosa ou neutra – tudo tem sua causa no ato de criação de um ser
superior.’ Existem brâmanes e contemplativos que acreditam nesse ensinamento,
que pensam dessa maneira: ‘Qualquer que seja a experiência de uma pessoa –
prazerosa, dolorosa ou neutra – tudo não tem uma causa e condição.’<o:p></o:p></span></p>


<p class=Normal> “Tendo me aproximado dos brâmanes e contemplativos que acreditam ...
qualquer que seja a experiência de uma pessoa ... tudo tem sua causa no que foi
feito no passado. ‘Eu lhes digo: ‘É verdade que vocês acreditam ... qualquer
que seja a experiência de uma pessoa ... tudo tem sua causa no que foi feito no
passado?’ Tendo sido inquiridos dessa forma por mim, eles admitem, ‘Sim.’ Então
lhes digo, ‘Então nesse caso, uma pessoa é uma assassina de seres vivos pelo
que foi feito no passado. Uma pessoa é uma ladra ... impura ... mentirosa ...
que causa desarmonia pelo que fala ... rude no que fala ... tagarela ...
cobiçosa ... tem má vontade ... tem entendimento incorreto por causa do que foi
feito no passado.’ Bhikkhus, quando uma pessoa retrocede ao que foi feito no
passado como sendo essencial, não existe desejo, não existe esforço [em relação
ao pensamento], ‘Isto deve ser feito. Isto não deve ser feito.’ Quando a
pessoa não consegue identificar com clareza, como verdade ou realidade, o que
deve e o que não deve ser feito, ela permanece num estado confuso e
desprotegido. Essa pessoa não pode referir-se a si mesma com justiça como uma
contemplativa. Essa foi minha primeira refutação irrepreensível a esses
brâmanes e contemplativos que acreditam nesse ensinamento, que pensam dessa
maneira.<span style='text-transform:uppercase'><o:p></o:p></span></span></p>


<p class=Normal> “Tendo me aproximado dos brâmanes e contemplativos que acreditam ...
qualquer que seja a experiência de uma pessoa ... tudo tem sua causa no ato de
criação de um ser superior. ‘Eu lhes digo: ‘É verdade que vocês acreditam ...
qualquer que seja a experiência de uma pessoa ... tudo tem sua causa no ato de
criação de um ser superior?’ Tendo sido inquiridos dessa forma por mim, eles
admitem, ‘Sim.’ Então lhes digo, ‘Então nesse caso, uma pessoa é uma assassina
de seres vivos pelo ato de criação de um ser superior. Uma pessoa é uma ladra
... impura .. .mentirosa ... que causa desarmonia pelo que fala ... rude no que
fala ... tagarela ... cobiçosa ... tem má vontade ... tem entendimento
incorreto por causa do ato de criação de um ser superior.’ Bhikkhus, quando uma
pessoa retrocede ao ato de criação de um ser superior como sendo essencial, não
existe desejo, não existe esforço [em relação ao pensamento], ‘Isto deve ser
feito. Isto não deve ser feito.’ Quando a pessoa não consegue identificar com
clareza, como verdade ou realidade,o que deve e o que não deve ser feito, ela
permanece num estado confuso e desprotegido. Essa pessoa não pode referir-se a
si mesma com justiça como uma contemplativa. Essa foi minha segunda refutação
irrepreensível a esses brâmanes e contemplativos que acreditam nesse
ensinamento, que pensam dessa maneira.<o:p></o:p></span></p>


<p class=Normal> “Tendo me aproximado dos brâmanes e contemplativos que acreditam ...
qualquer que seja a experiência de uma pessoa... tudo não tem uma causa e
condição. ‘Eu lhes digo: ‘É verdade que vocês acreditam ... qualquer que seja
a experiência de uma pessoa ... tudo não tem uma causa e condição?’ Tendo sido
inquiridos dessa forma por mim, eles admitem, ‘Sim.’ Então lhes digo, ‘Então
nesse caso, uma pessoa é uma assassina de seres vivos sem uma causa e condição. Uma pessoa é uma ladra ... impura ... mentirosa ...que causa desarmonia pelo
que fala ... rude no que fala ... tagarela ... cobiçosa ... tem má vontade ...
tem entendimento incorreto sem uma causa e condição.’ Bhikkhus, quando uma pessoa
retrocede a uma ausência de causa e<span style='mso-spacerun:yes'> 
</span>condição como sendo essencial, não existe desejo, não existe esforço [em
relação ao pensamento], ‘Isto deve ser feito. Isto não deve ser feito.’ Quando
a pessoa não consegue identificar com clareza, como verdade ou realidade, o que
deve e o que não deve ser feito, ela permanece num estado confuso e
desprotegido. Essa pessoa não pode referir-se a si mesma com justiça como uma
contemplativa. Essa foi minha terceira refutação irrepreensível a esses
brâmanes e contemplativos que acreditam nesse ensinamento, que pensam dessa
maneira.<o:p></o:p></span></p>


<p class=Normal> “Esses são os três tipos de associações sectárias que – quando
questionadas, pressionadas em dar suas razões e censuradas por pessoas sábias –
embora possam dar uma explicação contrária, permanecem presas à inércia.<o:p></o:p></span></p>


<p class=Normal> “Mas, esse Dhamma que eu ensino é incontestável, imaculado, perfeito,
sem críticas por sábios brâmanes e contemplativos. E qual é o Dhamma que eu
ensino que é incontestável, imaculado, perfeito, sem críticas por sábios brâmanes
e contemplativos? ‘Há esses seis elementos’ é o Dhamma que eu ensino e que é
incontestável, imaculado, perfeito, sem críticas por sábios brâmanes e
contemplativos.<span style='mso-spacerun:yes'>  </span>‘Há essas seis bases de
contato sensual’ é o Dhamma que eu ensino e que é incontestável, imaculado,
perfeito, sem críticas por sábios brâmanes e contemplativos. ‘Há esses dezoito
tipos de examinação mental’ é o Dhamma que eu ensino e que é incontestável,
imaculado, perfeito, sem críticas por sábios brâmanes e contemplativos. ‘Há
essas quatro nobres verdades’ é o Dhamma que eu ensino e que é incontestável,
imaculado, perfeito, sem críticas por sábios brâmanes e contemplativos. <o:p></o:p></span></p>


<p class=Normal>‘“Há esses seis elementos” é o Dhamma que eu ensino e que é
incontestável, imaculado, perfeito, sem críticas por sábios brâmanes e
contemplativos’: Assim foi dito. E com referência a que foi dito isso? Há esses
seis elementos: o elemento<span style='mso-spacerun:yes'>  </span>terra, o
elemento água, o elemento fogo, o elemento ar, o elemento<span
style='mso-spacerun:yes'>  </span>espaço, o elemento consciência. ‘“Há esses
seis elementos” é o Dhamma que eu ensino e que é incontestável, imaculado,
perfeito, sem críticas por sábios brâmanes e contemplativos’: Assim foi dito. E
com referência a isso é que foi dito. <o:p></o:p></span></p>


<p class=Normal> ‘”Há esses seis bases de contato sensual” é o Dhamma que eu ensino e
que é incontestável, imaculado, perfeito, sem críticas por sábios brâmanes e
contemplativos’: Assim foi dito. E com referência a que foi dito isso? Há
essas seis bases de contato sensual: o olho como uma base de contato sensual, o
ouvido<span style='mso-spacerun:yes'>  </span>como uma base de contato sensual,
o nariz<span style='mso-spacerun:yes'>  </span>como uma base de contato
sensual, a língua como uma base de contato sensual, o corpo como uma base de
contato sensual, a mente<span style='mso-spacerun:yes'>  </span>como uma base
de contato sensual. ‘“Há essas seis bases de contato sensual” é o Dhamma que eu
ensino e que é incontestável, imaculado, perfeito, sem críticas por sábios
brâmanes e contemplativos’: Assim foi dito. E com referência a isso é que foi
dito. <o:p></o:p></span></p>


<p class=Normal> ‘“Há esses dezoito tipos de examinação mental” é o Dhamma que eu ensino
e que é incontestável, imaculado, perfeito, sem críticas por sábios brâmanes e
contemplativos.’ Assim foi dito. E com referência a que foi dito isso? Ao ver
uma forma com o olho, alguém examina uma forma produtora de alegria, alguém
examina uma forma produtora de tristeza, alguém examina uma forma produtora de
equanimidade. Ao ouvir um som com o ouvido ... Ao cheirar um aroma com o nariz
... Ao saborear um sabor com a língua ... Ao tocar um tangível com o corpo ...
Ao conscientizar um objeto mental com a mente, alguém examina um objeto mental
produtor de alegria, alguém examina um objeto mental produtor de tristeza,
alguém examina um objeto mental produtor de equanimidade. ‘“Há esses dezoito
tipos de examinação mental” é o Dhamma que eu ensino e que é incontestável,
imaculado, perfeito, sem críticas por sábios brâmanes e contemplativos.’ Assim
foi dito. E com referência a isto é que foi dito.<o:p></o:p></span></p>


<p class=Normal> ‘”Há essas quatro nobres verdades” é o Dhamma que eu ensino e que é
incontestável, imaculado, perfeito, sem críticas por sábios brâmanes e
contemplativos.’ Assim foi dito. E com referência a que foi dito isso?<span
style='mso-spacerun:yes'>  </span>“Sustentado por/apegando-se aos seis
elementos, ocorre o estabelecimento de um embrião. Ocorrendo o estabelecimento
surge a mentalidade-materialidade (nome e forma). Da mentalidade-materialidade (nome e forma) como condição
surgem as seis bases dos sentidos. Das seis bases dos sentidos como condição
surge o contato. Do contato como condição surgem as sensações. Para a pessoa
que experimenta as sensações eu declaro, ‘Isso é sofrimento.’ Eu declaro, ‘Essa
é a origem do sofrimento.’ Eu declaro, ‘Essa é a cessação do sofrimento.’ Eu
declaro, ‘Esse é o caminho da prática que conduz à cessação do sofrimento.’<o:p></o:p></span></p>


<p class=Normal> “E qual é a nobre verdade do sofrimento? Nascimento é sofrimento,
envelhecimento é sofrimento, morte é sofrimento; tristeza, lamentação, dor,
angústia e desespero são sofrimento; a união com o que não se ama é sofrimento,
a separação do que se ama é sofrimento, não obter o que se deseja é sofrimento.
Em resumo, os cinco agregados influenciados pelo apego são sofrimento. Essa é
chamada a nobre verdade do sofrimento.<o:p></o:p></span></p>


<p class=Normal>
 “E qual é a nobre verdade da origem do sofrimento?<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>
 “Da ignorância como condição, as formações volitivas
[surgem]<br>


“Das
formações volitivas como condição, a consciência.<br>


“Da consciência como condição, a
mentalidade-materialidade (nome e forma).<br>


“Da mentalidade-materialidade (nome e forma) como condição,
as seis bases dos sentidos.<br>


“Dos seis bases dos sentidos como condição,
o contato.<br>


“Do contato como condição, as sensações.<br>


“Das sensações como condição, o desejo.<br>


“Do desejo como condição, o apego.<br>


“Do apego como condição, ser/existir.<br>


“Do ser/existir como condição, o nascimento.<br>

“Do nascimento como condição, o envelhecimento e a morte, tristeza,
lamentação, dor, angústia e desespero surgem. Essa é a origem de toda essa
massa de sofrimento.<o:p></o:p></span></p>


<p class=Normal>“Isso é chamado de nobre verdade da origem do sofrimento.<o:p></o:p></span></p>


<p class=Normal>“E qual é a nobre verdade da cessação do sofrimento?<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>
 “Com o completo
desaparecimento e cessação da ignorância, cessam as formações volitivas.<br>


“Com a cessação das
formações volitivas, cessa a consciência.<br>


“Com a cessação da
consciência, cessa a mentalidade-materialidade (nome e forma).<br>


“Com a cessação da
mentalidade-materialidade (nome e forma), cessam as seis bases dos sentidos.<br>


“Com a cessação das
seis bases dos sentidos, cessa o contato.<br>


“Com a cessação do
contato, cessa a sensação.<br>


“Com a cessação da
sensação, cessa o desejo.<br>


“Com a cessação do
desejo, cessa o apego.<br>


“Com a cessação do
apego, cessa o ser/existir.<br>


“Com a cessação do
ser/existir,<span style='mso-spacerun:yes'>  </span>cessa o nascimento.<br>

“Com a cessação do nascimento,<span style='mso-spacerun:yes'> 
</span>cessam o envelhecimento e morte, tristeza, lamentação, dor, angústia e
desespero. Essa é a cessação de toda essa massa de sofrimento.<o:p></o:p></span></p>


<p class=Normal>“Isso é chamado de a nobre verdade da cessação do sofrimento.<o:p></o:p></span></p>


<p class=Normal> “E qual é a nobre verdade do caminho da prática que conduz à cessação do
sofrimento? Exatamente este nobre caminho óctuplo: entendimento correto,
pensamento correto, linguagem correta, ação correta, modo de vida correto,
esforço correto, atenção plena correta, concentração correta. Isso<span
style='mso-spacerun:yes'>  </span>é chamado de a nobre verdade do caminho da
prática que conduz à cessação do sofrimento.<o:p></o:p></span></p>


<p class=Normal> “Há essas quatro nobres verdades”<span style='mso-spacerun:yes'> 
</span>é o Dhamma que eu ensino e que é incontestável, imaculado, perfeito, sem
críticas por sábios brâmanes e contemplativos:<span style='mso-spacerun:yes'> 
</span>Assim foi dito. E com referência a isto é que foi dito.”<o:p></o:p></span></p>

