<p class=Normal>Em certa ocasião, o Abençoado estava viajando ao longo da estrada entre Madhura e Veranja. Um número de homens e mulheres chefes de família também estava viajando ao longo da mesma estrada. Então, o Abençoado saiu da estrada e sentou-se ao pé de uma árvore. Os homens e mulheres chefes de família viram o Abençoado sentado ali e aproximaram-se dele, prestaram-lhe homenagem e sentaram a um lado. O Abençoado então lhes disse: </p>

<p class=Normal>"Chefes de família, existem essas quatro formas de convivência. Quais quatro? Um inferior vive com uma inferior, um inferior vive com uma deva; um deva vive com uma inferior, um deva vive com uma deva. </p>

<p class=Normal> (1) "E como, chefes de família, um inferior vive com uma inferior? Aqui, o marido é aquele que destrói a vida, toma o que não é dado, se engaja na conduta sexual imprópria, diz mentiras, e se entrega ao álcool, vinho e outros embriagantes, que causam a negligência; ele é imoral, de mau caráter; ele permanece em casa com o coração obcecado pela mácula de avareza; ele insulta e ofende contemplativos e brâmanes. E sua esposa também é aquela que destrói a vida ... ela insulta e ofende contemplativos e brâmanes. É assim que um inferior vive com uma inferior. </p>

<p class=Normal> (2) "E como é que um inferior vive com uma deva? Aqui, o marido é aquele que destrói a vida ... insulta e ofende contemplativos e brâmanes. Mas sua esposa é aquela que se abstém de destruição da vida, de tomar o que não é dado, da conduta sexual imprópria, da linguagem mentirosa, e do álcool, vinho e outros embriagantes, que causam a negligência; ela é virtuosa, de bom caráter; ela permanece em casa com um coração livre da mácula da avareza; ela não insulta ou ofende contemplativos e brâmanes. É assim que um inferior vive com uma deva. </p>

<p class=Normal> (3) "E como é que um deva vive com uma inferior? Aqui, o marido é aquele que se abstém da destruição da vida ... ele não insulta ou ofende contemplativos e brâmanes. Mas sua esposa é aquela que destrói a vida ... ela insulta e ofende contemplativos e brâmanes. É assim que um deva vive com uma inferior. </p>

<p class=Normal> (4) "E como é que um deva vive com uma deva? Aqui, o marido é aquele que se abstém da destruição da vida ... ele não insulta ou ofende contemplativos e brâmanes. E sua esposa também é aquela que se abstém da destruição da vida ... ela não insulta ou ofende contemplativos e brâmanes. É assim que um deva vive com uma deva. </p>

<p class=Normal>"Estas são as quatro formas de convivência." </p>

