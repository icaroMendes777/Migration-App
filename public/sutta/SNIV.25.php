<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Notas:<o:p></o:p></span></b></p>



<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] Os versos de desapontamento se referem ao encontro de Mara com o
Buda no sutta anterior. [<a href="#R1">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[2] Os seus nomes significam Desejo, Descontentamento e Cobiça. Elas
viram o pai deprimido e se aproximaram para descobrir qual a razão. [<a
href="#R2">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[3] A explicação do comentário: “Um elefante é capturado e conduzido
para fora da floresta usando uma fêmea como isca.” [<a href="#R3">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[4] O significado é “queremos estar sob o seu controle como escravas.” É
óbvio que essa é uma insinuação de caráter sexual. [<a href="#R4">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N5></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[5] <i>Cinco torrentes</i>: atravessou as torrentes das impurezas nas
cinco portas dos sentidos. <i>A sexta</i>: a mente. Ou como alternativa, as cinco
torrentes são os cinco primeiros grilhões e a sexta os outros cinco grilhões. [<a
href="#R5">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[6] <i>Corpo tranqüilo</i>: a tranqüilização da respiração no quarto
jhana. <i>Mente bem libertada: </i>com o fruto do estado de arahant. <i>Não
gerando: </i>não gerando os três tipos de formações volitivas (veja o <a
href="SNXII.51.php">SN XII.51</a>). <i>Meditando livre de pensamentos: </i>no
quarto jhana. <i>Ele não irrompe<span style='mso-spacerun:yes'>  </span>ou
deriva, ou enrijece: </i>não irrompe devido à raiva, não deriva devido à cobiça
e não enrijece devido à delusão. [<a href="#R6">Retorna</a>]<o:p></o:p></span></p>


