<p class=Normal style='margin-left:.75in'>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então,
Mara, o Senhor do Mal, tendo dito aqueles versos de desapontamento na presença
do Abençoado, saiu daquele lugar e sentou no chão com as pernas cruzadas, não muito
distante do Abençoado, silencioso, desanimado, com os ombros caídos, abatido,
chocado, incapaz de dizer uma palavra, raspando o chão com uma vara. [<a
href="#N1">1</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Então, as filhas de Mara - Tanha, Arati e Raga - foram até Mara, o Senhor
do Mal, e se dirigiram a ele em versos: [<a href="#N2">2</a>]<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><a name=R2></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>“Porque você está deprimido, pai? <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Quem é o homem pelo qual você sofre?<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Nós o agarraremos com a armadilha da cobiça<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>tal como se agarra um elefante da floresta.<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Nós o ataremos com firmeza e o traremos de volta,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>e ele estará sob o seu controle.” [<a href="#N3">3</a>]<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><a name=R3></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>[Mara:]<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“O Arahant, o Abençoado no mundo,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>não é atraído facilmente através da cobiça.<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Ele foi além do reino de Mara:<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>por isso sofro tão amargamente.”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, as filhas
de Mara - Tanha, Arati e Raga - foram até o Abençoado e disseram: “Nós o
servimos aos seus pés, contemplativo.” Mas o Abençoado não lhes deu atenção,
visto que ele estava libertado na insuperável extinção das aquisições.[<a
href="#N4">4</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Então, as filhas de Mara - Tanha, Arati e Raga – foram para um lado para
se aconselharem: “Os gostos dos homens são variados. E se cada uma de nós nos
manifestássemos sob a forma de cem virgens.” Então as filhas de Mara, cada uma
se manifestando sob a forma de cem virgens, foram até o Abençoado e disseram:
“Nós o servimos aos seus pés, contemplativo.” Mas o Abençoado não lhes deu
atenção, visto que ele estava libertado na insuperável extinção das aquisições.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, as
filhas de Mara foram para um lado para novamente se aconselharem: “Os gostos
dos homens são variados. E se cada uma de nós nos manifestássemos sob a forma
de cem mulheres que nunca deram à luz.”<span style='mso-spacerun:yes'> 
</span>Então as filhas de Mara, cada uma se manifestando sob a forma de cem
mulheres que nunca deram à luz ... sob a forma de cem mulheres que deram à luz
uma vez ... sob a forma de cem mulheres que deram à luz duas vezes ... sob a
forma de cem mulheres maduras .... sob a forma de cem mulheres velhas, foram
até o Abençoado e disseram: “Nós o servimos aos seus pés, contemplativo.” Mas o
Abençoado não lhes deu atenção, visto que ele estava libertado na insuperável
extinção das aquisições.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, as
filhas de Mara - Tanha, Arati e Raga – foram para um lado e disseram: “Aquilo que
o nosso pai nos disse é verdade:<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“’O Arahant, o Abençoado no mundo,...<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>por isso sofro tão amargamente.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se, com
tais táticas, tivéssemos assaltado qualquer contemplativo ou brâmane que não
estivesse desprovido de cobiça, ou o coração dele teria explodido, ou ele teria
vomitado sangue quente pela boca, ou teria ficado maluco ou mentalmente
perturbado; ou então, ele teria secado e murchado e teria ficado todo enrugado,
tal qual um capim verde que, quando cortado, seca e murcha e fica todo
enrugado.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, as
filhas de Mara - Tanha, Arati e Raga - foram até o Abençoado e ficaram em pé a
um lado e se dirigiram ao Abençoado em versos:<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“É por estar afundado na tristeza<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>que você medita na floresta?<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Porque você perdeu riqueza ou por ela anseia,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>ou cometeu algum crime no vilarejo?<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Porque você não faz amizade com as pessoas?<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Porque você não cria laços íntimos?”<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[O Abençoado:]<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Tendo conquistado a legião do prazeroso e do agradável, <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>meditando só, eu descobri a bem-aventurança,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>a realização do objetivo, a paz no coração.<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Portanto, eu não faço amizades com as pessoas,<span
style='mso-spacerun:yes'>   </span><br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>nem crio laços íntimos.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, a
filha de Mara, Arati, se dirigiu ao Abençoado em versos: <o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Como é que aqui um bhikkhu permanece frequentemente, <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>de modo que, cruzadas as cinco torrentes, ele tenha aqui cruzado a
sexta? <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Como ele medita de modo que as percepções sensuais<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>sejam mantidas à distância e deixem de<span style='mso-spacerun:yes'> 
</span>aprisioná-lo?[<a href="#N5">5</a>]<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><a name=R5></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>[O Abençoado:]<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Com o corpo tranqüilo e com a mente bem libertada, <br>
não gerando, com atenção plena, na vida santa,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>conhecendo o Dhamma, meditando livre de pensamentos,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>ele não irrompe ou deriva, ou enrijece. [<a href="#N6">6</a>]<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><a name=R6></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>“Quando aqui um bhikkhu assim permanece
frequentemente,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>tendo cruzado as cinco torrentes, ele aqui cruza a sexta. <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Quando ele assim medita, as percepções sensuais<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>são mantidas à distância e deixam de aprisioná-lo.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, a
filha de Mara, Raga, se dirigiu ao Abençoado em versos:<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Ele decepou o desejo, com o seu grupo e ordem; <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>com certeza muitos outros seres irão cruzar.<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Ai de mim, esse contemplativo irá arrebatar muitas pessoas<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>e conduzí-las para além do Rei da Morte.”<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[O Abençoado:]<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Deveras os Tathagatas, os grandes heróis,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>lideram por meio do verdadeiro Dhamma.<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Quando eles lideram por meio do Dhamma,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>que inveja pode haver naqueles que compreendem?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, as
filhas de Mara - Tanha, Arati e Raga - foram até Mara, o Senhor do Mal. Mara as
viu vindo à distância e se dirigiu a elas em versos:<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Tolas! Vocês tentaram bater numa montanha<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>com talos de flores de lótus,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>cavar uma montanha com as suas unhas,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>mastigar ferro com os seus dentes.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Como se, tendo levantado uma pedra com a cabeça,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>vocês buscassem por um apoio no abismo;<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>como se golpeassem um toco com o peito,<span style='mso-spacerun:yes'> 
</span><br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>vocês, desapontadas, deixaram Gotama.”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Elas vieram até ele com a beleza deslumbrante - 

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Tanha, Arati e Raga -

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>mas o Mestre as varreu ali mesmo, 

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>tal qual o vento varre um tufo de algodão caído.<o:p></o:p></span></p>



