<p class=Normal> “Bhikkhus, há esses dois tipos de enfermidade. Quais duas? Enfermidade do corpo e enfermidade da mente. Podem ser encontradas pessoas que afirmam desfrutar de saúde do corpo durante um, dois, três, quatro, ou cinco anos; por dez, vinte, trinta, quarenta, ou cinquenta anos; e até mesmo por cem anos ou mais. Mas exceto aqueles que destruíram as impurezas, é difícil encontrar pessoas no mundo que podem afirmar desfrutar de saúde na mente, mesmo que seja apenas por um momento.  </p>

<p class=Normal> "Bhikkhus, um bhikkhu está sujeito a essas quatro enfermidades. Quais quatro? (1) Aqui, um bhikkhu tem desejos intensos, experimenta aborrecimentos, e não está satisfeito  com nenhum tipo de manto, comida esmolada, moradia, ou medicamentos para os enfermos. (2) Porque ele tem desejos intensos, experimenta aborrecimentos, e não está satisfeito  com nenhum tipo de manto, comida esmolada, moradia, ou medicamentos para os enfermos, ele se submete a desejos ruins por reconhecimento, e por ganho, fama e honrarias. (3) Ele se estimula, intenta, se esforça para obter reconhecimento, e por ganho, fama e honrarias. (4) Com astúcia ele se aproxima das famílias, com astúcia ele senta, com astúcia ele explica o Dhamma, e com astúcia ele retém a urina e o excremento. Essas são as quatro enfermidades incorridas por um bhikkhu.</p>

<p class=Normal> "Portanto, bhikkhus, assim vocês devem treinar:  'Nós não teremos desejos intensos e não experimentaremos aborrecimentos, e não ficaremos insatisfeitos com qualquer tipo de manto, comida esmolada, moradia, ou medicamentos para os enfermos. Não nos submeteremos a desejos ruins por reconhecimento, e por ganho, fama e honrarias. Nós não nos estimularemos, intentaremos, nos esforçaremos para obter reconhecimento, e por ganho, fama e honrarias. Nós pacientemente suportaremos o calor, a fome e a sede; o contato com moscas, mosquitos, vento, sol e  criaturas rastejantes; linguagem rude e ofensiva; nós suportaremos sensações corporais que são dolorosas, torturantes, penetrantes, cortantes, desagradáveis, agonizantes e que ameaçam a vida.' É desse modo, bhikkhus, que vocês devem treinar. </p>

