<p class=Normal><a name=R1></a> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: "Bhikkhus, por estar tomado, com a mente obcecada por três condições prejudiciais Devadatta irá inevitavelmente para um estado miserável, para o inferno, pela duração de um éon. Quais são as três? Tomado, com a sua mente obcecada por desejos ruins ...; tomado, com a sua mente obcecada por amizades ruins; e embora houvesse mais que pudesse ter sido realizado, ele parou no meio do caminho devido a ter alcançado uma realização supra-humana sem importância [<a href="#N1">1</a>]. Essas bhikkhus são as três."</p>

<p class=Normal style='margin-left:.75in'> Com certeza alguém com desejos ruins<br>
não renasce neste mundo. <br>
Saiba que ele irá para o plano<br>
daqueles que vivem obcecados pelos desejos ruins.</p>

<p class=Normal style='margin-left:.75in'> Eu ouvi quando Devadatta<br> era
considerado um homem sábio, <br>
com a meditação desenvolvida<br>
que brilhava com a fama.</p>

<p class=Normal style='margin-left:.75in'> Tendo pensado ser seu igual<br>
ele atacou o Tathagata<br>
e acabou destinado ao temível<br>
Avici, o inferno.</p>

<p class=Normal style='margin-left:.75in'> Quando alguém trama contra um inocente<br>
que não fez nenhum mal, <br>
o mal apenas afeta aquele<br>
com a mente corrompida e desrespeitoso.</p>

<p class=Normal style='margin-left:.75in'> Quem pensa ser capaz de poluir <br>
o oceano com um balde de veneno<br>
seria incapaz de fazê-lo -<br>
tão impressionante é essa massa de água.</p>

<p class=Normal style='margin-left:.75in'> É o mesmo que atacar com insultos<br>
o Tathagata que alcançou a perfeição<br>
e que sempre desfruta da mente pacífica -<br>
insultos não o afetam em nada. <br>
Uma pessoa sábia deveria cultivar essa amizade<br>
e constantemente acompanhá-lo. <br>
Um bhikkhu que segue o seu caminho<br>
poderá dar um fim ao sofrimento.</p>


