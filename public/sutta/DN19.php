<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião, o Abençoado estava<span style='mso-spacerun:yes'> 
</span>em Rajagaha, no Pico do Abutre. Então, quando a noite estava bem
avançada, o gandhabba Pancasikha [<a href="#N1">1</a>] <a name=R1></a>iluminou
todo o Pico do Abutre com uma belíssima luminosidade [<a href="#N2">2</a>] <a
name=R2></a>e se aproximou do Abençoado. Ao se aproximar ele homenageou o
Abençoado e ficando em pé a um lado Pancasikha disse: “Venerável senhor, eu
gostaria de relatar aquilo que vi e observei pessoalmente quando estava na
presença dos devas do Trinta e Três.” - “Diga Então, Pancasikha”, o Abençoado
disse.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. -3.
“Venerável senhor, no passado, há muito tempo, no uposatha do décimo quinto
dia, no final da estação das chuvas, na noite de lua cheia, todos os devas do
Trinta e Três estavam reunidos no Salão Sudhamma ... (igual ao <a
href="DN18.php#T12">DN 18.12</a>) ... E por essa razão os devas do Trinta e Três ficavam 
satisfeitos, felizes, cheios de prazer e alegria, dizendo: “A hoste dos devas está crescendo, 
a hoste dos asuras está diminuindo!”. Então, Sakka recitou estes versos:<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>‘Os devas do Trinta e Três se alegram, o seu líder também, <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>elogiando o Tathagata e a verdade do Dhamma,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>vendo devas recém-chegados, luminosos e gloriosos<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>que viveram a vida santa, agora bem renascidos.<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Eclipsando todos os demais em fama e esplendor,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>os discípulos distinguidos do Sábio poderoso.<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Vendo isso, os devas do Trinta e Três se alegram, o seu líder também,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>elogiando o Tathagata e a verdade do Dhamma.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em vista disso,
os devas do Trinta e Três se alegraram ainda mais dizendo: ‘A hoste dos devas
está crescendo, a hoste dos asuras está diminuindo!’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4.
[Pancasikha continua:] “Então, Sakka, vendo a satisfação deles, disse para os
devas do Trinta e Três: ‘Senhores, vocês gostariam de ouvir oito afirmações
verdadeiras em louvor do Abençoado?’ E obtendo a concordância deles ele
declarou: <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. “’O que
pensam os senhores do Trinta e Três? Com relação ao modo como o Abençoado se
esforçou pelo bem-estar e pela felicidade de devas e humanos, por compaixão
pelo mundo e pelo bem-estar e felicidade de muitos<span
style='mso-spacerun:yes'>  </span>– não podemos encontrar um outro mestre
dotado dessas qualidades, quer consideremos o passado ou o presente, que não
seja o Abençoado.<span style='mso-spacerun:yes'>  </span><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. “’Bem
proclamado, deveras, é o Dhamma do Abençoado, visível no aqui e agora, com
efeito imediato, que convida ao exame, que conduz para adiante, para ser
experimentado pelos sábios por eles mesmos – e não podemos encontrar nenhum
outro proclamador de uma doutrina que conduza para adiante como essa, quer seja
no passado ou no presente, que não seja o Abençoado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. “’O
Abençoado explicou bem o que é correto e o que é incorreto, o que é censurável
e o que não é, o que deve ser praticado e o que não deve, o que é vulgar e o
que é nobre, o que tem qualidade inferior, superior ou mista. [<a href="#N3">3</a>]
<a name=R3></a>E não podemos encontrar nenhum outro proclamador dessas coisas
.... que não seja o Abençoado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8.
“’Novamente, o Abençoado explicou bem para os seus discípulos o caminho que
conduz a Nibbana, [<a href="#N4">4</a>] <a name=R4></a>e eles coalescem,
Nibbana e o caminho, igual às águas do Ganges e do Yamuna coalescem e fluem
juntas. E não podemos encontrar nenhum outro proclamador do caminho para<span
style='mso-spacerun:yes'>  </span>nibbana .... que não seja o Abençoado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. “’ E o
Abençoado obteve companheiros, tanto treinandos [<a href="#N5">5</a>] <a
name=R5></a>como aqueles que, tendo vivido a vida santa eliminaram as
impurezas, [<a href="#N6">6</a>] <a name=R6></a>e o Abençoado permanece junto
deles, todos regozijando-se naquela única coisa. <span
style='mso-spacerun:yes'> </span>E não podemos encontrar nenhum outro mestre
.... que não seja o Abençoado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. “’As
oferendas dadas ao Abençoado são bem dadas, a fama dele está bem estabelecida,
tanto assim que, eu penso, os Khattiyas continuarão apegados a ele, no entanto
o Abençoado aceita as suas esmolas de alimentos sem presunção. E não podemos
encontrar nenhum outro mestre que faça isso.... que não seja o Abençoado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. “’E o
Abençoado age de acordo com o que ele fala, e fala de acordo com as suas ações.
E não podemos encontrar nenhum outro mestre que faça isso, com relação a cada
detalhe da doutrina .... que não seja o Abençoado. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. “’O
Abençoado superou a dúvida, foi além do ‘como’ e ‘porque’, ele realizou o
objetivo da vida santa suprema.<span style='mso-spacerun:yes'>  </span>E não
podemos encontrar nenhum outro mestre que tenha feito isso, quer consideremos o
passado ou o presente, que não seja o Abençoado.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E quando
Sakka assim proclamou essas oito afirmações verdadeiras em louvor do Abençoado,
os devas do Trinta e Três ficaram ainda mais satisfeitos, felizes, cheios de
prazer e alegria com aquilo que haviam ouvido em louvor ao Abençoado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. “Então,
alguns devas exclamaram: ‘Ah, se pelo menos quatro Budas perfeitamente
iluminados surgissem no mundo e ensinassem o Dhamma como o Abençoado! Isso
seria pelo bem-estar e pela felicidade de muitos, por compaixão pelo mundo,
pelo bem-estar e felicidade de devas e humanos!’ E alguns disseram: ‘Que não
fossem quatro Budas perfeitamente iluminados – três bastariam!’ E outros
disseram: ‘Que não fossem três – dois bastariam!’ <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14. “Com
relação a isso Sakka disse: ‘É impossível, senhores, não pode acontecer que
dois Budas perfeitamente iluminados, possam surgir ao mesmo tempo no mundo -
não existe essa possibilidade. Que este Abençoado continue a viver por muitos
anos, livre da doença e enfermidade! Isso seria pelo bem-estar e pela
felicidade de muitos, por compaixão pelo mundo, pelo bem-estar e felicidade de
devas e humanos!’ <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E Então,
os devas do Trinta e Três consultaram e deliberaram juntos sobre o assunto pelo
qual eles haviam se reunido no salão Sudhamma, e os Quatro Grandes Reis
aconselharam e alertaram com relação a esse assunto, permanecendo imóveis nos
seus assentos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15-16. “Uma
luminosidade gloriosa surgiu, surgiu do norte e o esplendor visto era mais intenso que o brilho dos devas
E Sakka disse para os devas do Trinta e Três: “Senhores, quando esses sinais são vistos, quando essa 
luz aparece e surge um brilho, então Brahma irá aparecer. Pois esses sinais são os 
presságios da aparição de Brahma.” ... (igual ao <a href="DN18.php#T15">DN 18.15-17</a>).<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17. “Então,
o Brahma Sanankumara, tendo descido do seu mundo, e vendo o contentamento
deles, recitou estes versos: <o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>‘Os devas do Trinta e Três se alegram, o seu líder também ... ’ (igual
verso 2 acima)<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18. “Agora
com relação ao discurso do Brahma Sanankumara ... (igual ao <a
href="DN18.php#T19">DN 18.19</a>).<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>19. “Então,
os devas do Trinta e três disseram para o Brahma Sanankumara: ‘Muito bem,
Brahma! Nós nos regozijamos com aquilo que ouvimos. Sakka, o senhor dos devas
também nos declarou oito afirmações verdadeiras em louvor do Abençoado, e nós
também nos regozijamos com isso.’ Então, Brahma disse para Sakka: ‘Muito bem,
senhor dos devas. E nós também gostaríamos de ouvir essas oito afirmações
verdadeiras em louvor do Abençoado.’ ‘Muito bem, Grande Brahma’, Sakka disse, e
ele repetiu aquelas oito afirmações: <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>20.-27. “’O
que você pensa, venerável Brahma ...? (igual aos versos 5-12). E o Brahma
Sanankumara ficou satisfeito, feliz, cheio de prazer e alegria com aquilo que
ouviu em louvor do Abençoado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>28. “Então,
o Brahma Sanankumara depois de assumir uma forma mais grosseira, apareceu para os devas do 
Trinta e Três com a forma do jovem Pancasikha ... (igual ao <a href="DN18.php#T18">DN18.18</a>) [<a href="#N7">7</a>]
<a name=R7></a> ... E assim de pernas cruzadas ele disse para os devas do Trinta e
Três: ‘Há quanto tempo o Abençoado possui poderosa sabedoria?’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>29. “’Certa
vez, houve um rei chamado Disampati. O seu ministro era um Brâmane chamado
Administrador. [<a href="#N8">8</a>]<a name=R8></a> O filho do rei era um jovem
chamado Renu e o filho do administrador se chamava Jotipala. O príncipe Renu e
Jotipala, junto com outros seis Khattiyas, formavam um grupo de oito amigos.
Depois de um certo tempo o Administrador morreu e o Rei Disampati enlutado
dizia: “Ai de mim, exatamente agora que havíamos transferido todas as nossas
responsabilidades para o Administrador e estávamos nos entregando aos prazeres
dos cinco sentidos, o Administrador morreu!”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’Ouvindo
isso, o príncipe Renu disse: “Senhor, não se entristeça tanto pela morte do
Administrador! O filho dele, Jotipala, é mais esperto que o pai era e sabe
distinguir bem o que é vantajoso. Você deveria deixar que Jotipala administre
todos os negócios que você havia delegado ao pai dele.” - “É verdade isso, meu
filho?” - “Sim, senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>30.
“’Então, o Rei chamou um homem e disse: “Venha, meu bom homem, vá até o jovem
Jotipala e diga: ‘Que o venerável Jotipala esteja bem! o Rei Disampati o chama,
ele gostaria de vê-lo.’” - “Muito bem, Majestade”, o homem respondeu, e comunicou
a mensagem. Ao receber a mensagem, Jotipala disse: “Muito bem, senhor’, e foi
ver o Rei. Ao chegar na presença do Rei eles se cumprimentaram de forma cortês
e ele sentou a um lado. O Rei disse: “Desejamos que o venerável Jotipala
administre os nossos negócios. Não recuse. Eu o colocarei no lugar do seu pai e
o ungirei como Administrador.” - “Muito bem, venerável senhor”, Jotipala
respondeu. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>31.
“’Assim, o rei Disampati nomeou Jotipala como administrador no lugar do seu
pai. E uma vez nomeado, Jotipala realizou os negócios que o seu pai realizava,
não fazendo nenhum tipo de negócio que o seu pai não havia feito. Ele completou
todas as tarefas que o seu pai havia contemplado, nenhuma outra. E as pessoas
diziam: “Esse Brâmane é deveras um administrador! De fato ele é um grande
administrador!” E assim foi como o jovem Brâmane Jotipala passou a ser
conhecido como o Grande Administrador. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>32. “’E um
certo dia, o Grande Administrador foi até o grupo de seis nobres e disse: “O
rei Disampati está velho, com a idade avançada, atribulado pelos anos, avançado na vida, já no
último estágio. A sua vida está
próxima do fim e ele não irá durar muito mais. Quem poderá dizer quanto tempo
as pessoas irão viver? Quando o rei Disampati morrer, os ‘fazedores de reis’ [<a
href="#N9">9</a>]<a name=R9></a> com certeza irão ungir o príncipe Renu como
rei. Vocês devem ir até o príncipe Renu e dizer: ‘Nós somos os amigos amados,
estimados e favoritos do príncipe Renu, compartindo das suas alegrias e
tristezas. Nosso rei Disampati está velho ... Quando ele morrer, os fazedores
de reis com certeza irão ungir o príncipe Renu como rei. Se o príncipe Renu
obtiver o reinado, que ele o comparta conosco.’” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>33. “’Muito
bem, senhor’, os seis nobres disseram e foram até o príncipe Renu e falaram com
ele da forma proposta pelo Grande Administrador. ‘Muito bem, senhores, quem,
além de mim, deveria prosperar? Vocês. Senhores, se eu obtiver o reinado,
comparti-lo-ei com vocês.’ <span style='mso-spacerun:yes'> </span><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>34. “’No
seu devido tempo o rei Disampati morreu e os fazedores de reis ungiram o
príncipe Renu como rei. E tendo sido feito rei, Renu se entregou aos prazeres
dos cinco sentidos. Então, o Grande Administrador foi até os seis nobres e disse:
“Senhores, agora que o rei Disampati morreu, o príncipe Renu que foi ungido
como rei, se entregou aos prazeres dos cinco sentidos. Quem sabe o que irá
resultar disso? Os prazeres sensuais são embriagantes. Vocês deveriam ir ter
com ele e dizer: ‘O Rei Disampati está morto e o príncipe Renu foi ungido como
Rei. Você lembra da sua promessa, senhor?’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’Eles
assim fizeram e o Rei disse: “Senhores, eu lembro da minha palavra. Quem é
capaz de dividir este reino poderoso tão largo no norte e tão estreito no sul,
em sete partes iguais?” - “Quem de fato, senhor, exceto o Grande Administrador?” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>35. “”Assim
o rei Renu mandou um homem até o Grande Administrador dizendo: “Senhor, o Rei o
chama.” O homem foi e o Grande Administrador veio até o Rei e depois de cumprimentá-lo
com cortesia sentou a um lado. Então, o Rei disse: “Venerável Administrador, vá
e divida este reino poderoso, tão largo no norte e tão estreito no sul, em sete
partes iguais.” - “Muito bem, Senhor,” disse o Grande Administrador, e assim ele
fez.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>36. “’ E o
reino de Renu estava no centro:<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Dantapura vai para os Kalingas, Potaka para os Assakas,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Mahissati para os Avantis, Roruka para os Soviras.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Mithila para os Videhas, Campa para os Angas,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Benares para os Kasi, assim distribuiu o Administrador.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os seis
nobres ficaram satisfeitos com os seus ganhos respectivos e o êxito do plano:
“Aquilo que queríamos, desejávamos, objetivávamos e nos esforçamos para
conseguir, nós obtivemos!”<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Sattabhu, Brahmadatta, Vessabhu e Bharata, <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Renu e dois Dhataratthas, esses são os sete <br>

<span lang=PT-BR
style='mso-ansi-language:PT-BR'>reis Bharat.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>[<i>Fim da
primeira recitação</i>]</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>37.
“’Então, os seis nobres foram até o Grande Administrador e disseram:<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
Administrador, você tem sido um amigo fiel, sincero e amado pelo Rei Renu e
assim também por nós. Por favor administre os nossos negócios! Nós esperamos
que você não recuse.” Assim ele administrou os reinos de sete reis ungidos e
ele ensinou os mantras para sete Brâmanes ilustres e setecentos pupilos
avançados. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>38. “’Com o
passar do tempo, esta boa reputação havia se espalhado com relação ao Grande
Administrador: “O Grande Administrador pode ver Brahma com os seus próprios
olhos, conversa com ele cara a cara e o consulta!” E ele pensou: “Agora essa
boa reputação tem se espalhado a meu respeito, que eu posso ver Brahma com os
meus próprios olhos, ... mas isso não é verdade. No entanto, eu ouvi, dito por
anciãos e Brâmanes respeitados, mestres de mestres, que qualquer um que se
isole em meditação durante os quatro meses das chuvas, desenvolvendo a absorção
na compaixão, poderá ver Brahma com os próprios olhos, conversar com ele cara a
cara e consultá-lo. E se eu fizesse isso!” [<a href="#N10">10</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R10></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>39. “’Assim, o Grande Administrador foi até o Rei Renu e relatou o que havia
acontecido e do seu desejo de ir para um retiro e desenvolver a absorção na
compaixão. “E ninguém deve se aproximar de mim, exceto para me trazer comida.”
- “Venerável Administrador, faça como julgar adequado.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>40. “’Os
seis nobres responderam da mesma forma: “Venerável Administrador, faça como
julgar adequado.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>41. “’Ele
foi até os sete Brâmanes e os setecentos pupilos e contou sobre a sua intenção,
