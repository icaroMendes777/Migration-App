<p class=Normal> “Um bhikkhu dedicado ao treinamento da mente superior, no momento apropriado, deve dar atenção a três sinais. Ele deve no momento apropriado dar atenção ao sinal da concentração, no momento apropriado dar atenção ao sinal do esforço energético, no momento apropriado dar atenção ao sinal da equanimidade. </p> 

<p class=Normal> “Se um bhikkhu dedicado ao treinamento da mente superior der exclusiva atenção ao sinal da concentração, é possível que a mente dele seja capturada pela indolência. [<a href="#N1">1</a>] <a name="R1"></a> Se ele der exclusiva atenção ao sinal do esforço energético, é possível que a mente dele seja capturada pela inquietação. Se ele der exclusiva atenção ao sinal da equanimidade, é possível que a mente dele não fique bem concentrada para a destruição das impurezas. </p>

<p class=Normal> “Mas, se no momento apropriado, ele der atenção para cada um desses três sinais, então a mente dele estará maleável, manuseável e luminosa, e ela estará bem concentrada para a destruição das impurezas. </p> 

<p class=Normal>Suponha que um ourives habilidoso ou seu aprendiz preparasse uma fornalha, aquecesse um cadinho, e tomando ouro com um par de tenazes, o colocasse no cadinho. Ele sopraria periodicamente, borrifaria água periodicamente, examinaria periodicamente. Se o ourives soprasse continuamente a fornalha poderia se aquecer em demasia. Se ele borrifasse água continuamente a fornalha poderia se esfriar em demasia. Se ele apenas examinasse, o ouro não ficaria perfeitamente purificado. Mas, se no momento apropriado o ourives realiza cada uma dessas três funções, o ouro estará maleável, manuseável e luminoso, e com as condições apropriadas para ser trabalhado. Qualquer ornamento que o ourives queira fazer, quer seja uma diadema, brincos, um colar ou uma corrente, o ouro poderá agora ser usado para esse fim. </p>

<p class=Normal>De modo semelhante, há esses três sinais aos quais um bhikkhu dedicado ao treinamento da mente superior deve dar atenção no momento apropriado, isto é, o sinal da concentração, o sinal do esforço energético e o sinal da equanimidade. Se ele der atenção a esses sinais com regularidade então a mente  dele estará maleável, manuseável e luminosa, e ela estará bem concentrada para a destruição das impurezas. </p>

<p class=Normal>Então, ele dirige a sua mente para qualquer estado que possa ser compreendido através do conhecimento direto, ele obtém a capacidade de compreender esse estado através do conhecimento direto, sempre que as condições necessárias estiverem presentes. </p>

