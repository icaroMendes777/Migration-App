<p class=Normal> "Bhikkhus, médicos prescrevem um purgante para eliminar as enfermidades que se originam da bílis, fleuma, e do vento. Há esse purgante, bhikkhus, eu não nego isso. No entanto, esse purgante algumas vezes funciona e algumas vezes fracassa. </p>


<p class=Normal>"Mas eu lhes ensinarei, bikkhus, um nobre purgante que sempre funciona, nunca fracassa. Na dependência desse purgante, seres sujeitos ao nascimento estarão livres do nascimento; seres sujeitos ao envelhecimento estarão livres do envelhecimento; seres sujeitos à morte estarão livres da morte; seres sujeitos à tristeza, lamentação, dor, angústia e desespero estarão livres da tristeza, lamentação, dor, angústia e desespero. Então ouçam e prestem muita atenção àquilo que eu vou dizer.” </p>


<p class=Normal> “Sim, venerável senhor,” os bhikkhus responderam. O Abençoado disse o seguinte: </p>


<p class=Normal>"Qual, bhikkhus é esse nobre purgante que sempre funciona, nunca fracassa? </p>


<p class=Normal> (1) "Quem tem entendimento correto, o entendimento incorreto é purgado, e as inúmeras qualidades ruins e prejudiciais que surgem tendo o entendimento incorreto como condição também são purgadas, e tendo o entendimento correto como condição, inúmeras qualidades benéficas são realizadas através do desenvolvimento. </p>


<p class=Normal> (2) - (9) "Quem tem pensamento correto, o pensamento incorreto é purgado ... quem tem linguagem correta, a linguagem incorreta é purgada ... quem tem ação correta, a ação incorreta é purgada ... quem tem modo de vida correto, o modo de vida incorreto é purgado ... quem tem esforço correto, o esforço incorreto é purgado ... quem tem atenção plena correta, a atenção plena incorreta é purgada ... quem tem concentração correta, a concentração incorreta é purgada ... quem tem conhecimento correto, o conhecimento incorreto é purgado ... </p>


<p class=Normal> (10) "Quem tem libertação correta, a libertação incorreta é purgada, e as inúmeras qualidades ruins e prejudiciais que surgem tendo a libertação incorreta como condição também são purgadas, e tendo a libertação correta como condição, inúmeras qualidades benéficas são realizadas através do desenvolvimento. </p>


<p class=Normal>"Esse, bhikkhus, é o nobre purgante que sempre funciona, nunca fracassa, e na dependência desse purgante, seres sujeitos ao nascimento estarão livres do nascimento; seres sujeitos ao envelhecimento estarão livres do envelhecimento; seres sujeitos à morte estarão livres da morte; seres sujeitos à tristeza, lamentação, dor, angústia e desespero estarão livres da tristeza, lamentação, dor, angústia e desespero."
</p>


