<p class=Normal> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: “Bhikkhus, esses dois irão para um plano de miséria, para o inferno se não abandonarem esse tipo de conduta. Quais dois? Aquele que não vivendo a vida santa faz de conta ser alguém que vive a vida santa, e aquele que falsamente acusa alguém que vive a vida santa na sua mais completa pureza de não vivê-la. Esses dois irão para um plano de miséria, para o inferno se não abandonarem esse tipo de conduta."</p>


<p class=Normal style='margin-left:.75in'> O falso acusador vai para o inferno <br>
também aquele que nega o que fez; <br>
ambos são iguais no além, <br>
pessoas com ações vis no mundo além.</p>

<p class=Normal style='margin-left:.75in'> Muitos impostores vestem o mando de cor ocre<br>
embora com caráter ruim e descontrolados. <br>
Devido às suas ações ruins, <br>
esses malfeitores nascem no inferno.</p>

<p class=Normal style='margin-left:.75in'> Melhor que ele engula <br>
uma bola de ferro incandescente<br>
do que esse imoral descontrolado<br>
se alimentar de comida esmolada.</p>




