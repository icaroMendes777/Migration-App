<p class=Normal> “’Nibbana é visível no aqui e agora,’ assim dizem. De que modo o Abençoado explicou nibbana é visível no aqui e agora?" </p>

<p class=Normal> (1) - (8)  "Aqui, amigo, afastado dos prazeres sensuais, afastado das qualidades não hábeis, um bhikkhu entra e permanece no primeiro jhana ... segundo jhana ... terceiro jhana ...  quarto jhana ... base do espaço infinito ... base da consciência infinita ... base do nada ... base da nem percepção, nem não percepção, foi com referência a isso que o Abençoado se referiu a nibbana visível no aqui e agora num sentido provisório. </p>

<p class=Normal> (9) "Outra vez, amigo, com a completa superação da base da nem percepção, nem não percepção, um bhikkhu entra e permanece na cessação da percepção e sensação, e tendo visto com sabedoria, as suas impurezas são destruídas, foi com referência a isso que o Abençoado se referiu a nibbana visível no aqui e agora num sentido não-provisório." </p>

