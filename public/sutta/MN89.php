<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião o Abençoado estava entre os Sakyas numa cidade
denominada Medalumpa.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. Agora,
naquela ocasião o Rei Pasenadi de Kosala havia chegado em Nagaraka para tratar
de negócios. Então ele se dirigiu a Digha Karayana:[<a href="#N1">1</a>] <a
name=R1></a>“Estimado Karayana, prepare as carruagens reais. Vamos até o jardim
das delícias e lá buscaremos um lugar agradável.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
senhor,” Digha Karayana respondeu. Quando as carruagens reais estavam
preparadas ele informou o rei: “Senhor, as carruagens reais estão preparadas.
Agora é o momento, faça como julgar adequado.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. Então o
Rei Pasenadi montou na carruagem real e acompanhado por outras carruagens ele
saiu de Nagaraka com toda a pompa da realeza e foi na direção do parque. Ele
foi até onde a estrada permitia o acesso das carruagens e depois desmontou da
sua carruagem e entrou no parque a pé. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. Enquanto
caminhava e perambulava pelo parque fazendo exercício, o Rei Pasenadi viu árvores belas
e inspiradoras, numa atmosfera de isolamento, serena e sem a
perturbação de vozes, afastada das pessoas, favorável ao retiro. A visão disso
fez com que ele se recordasse do Abençoado assim: “Essas árvores são
belas e inspiradoras, numa atmosfera de isolamento, serena e sem a perturbação
de vozes, afastada das pessoas, favorável ao retiro, como os lugares nos quais
costumávamos homenagear o Abençoado, um arahant, perfeitamente iluminado.” Então
ele disse para Digha Karayana o que ele havia pensado e perguntou: “Onde ele
está agora, o Abençoado, um arahant, perfeitamente iluminado?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. “Senhor,
há uma cidade dos Sakyas denominada Medalumpa. O Abençoado, um arahant,
perfeitamente iluminado, está lá, agora.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Qual a
distância de Nagaraka até Medalumpa?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não é
muito longe, senhor, são três léguas. [<a href="#N2">2</a>] <a name=R2></a>Ainda
há luz do dia suficiente para ir até lá.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
estimado Karayana, prepare as carruagens reais. Vamos partir para ver o
Abençoado, um arahant, perfeitamente iluminado.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim
senhor,” ele respondeu. Quando as carruagens reais estavam preparadas ele
informou o rei: “Senhor, as carruagens reais estão preparadas. Agora é o
momento, faça como julgar adequado.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. Então o
Rei Pasenadi montou na carruagem real e acompanhado por outras carruagens ele
saiu de Nagaraka na direção da cidade Sakya denominada Medalumpa. Ele lá chegou
quando ainda era dia e foi em direção ao parque. Ele foi até onde a estrada
permitia o acesso das carruagens e depois desmontou da sua carruagem e entrou
no parque a pé.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. Agora,
naquela ocasião muitos bhikkhus estavam caminhando para cá e para lá ao ar
livre. Então, o Rei Pasenadi foi até eles e perguntou: “Veneráveis senhores,
onde ele está agora, o Abençoado, um arahant, perfeitamente iluminado? Nós queremos
ver o Abençoado, um arahant, perfeitamente iluminado.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. “Aquela
é a sua habitação, grande rei, com a porta fechada. Vá até lá em silêncio, sem
pressa, entre na varanda, limpe a garganta e bata na porta. O Abençoado abrirá
a porta.” O Rei Pasenadi naquele momento entregou a sua espada e turbante para
Digha Karayana. Então Digha Karayana pensou: “Então o rei irá ter agora uma
sessão secreta! E eu terei que esperar aqui sozinho!”[<a href="#N3">3</a>] <a
name=R3></a>Sem se apressar, o Rei Pasenadi foi em silêncio até a habitação com
a porta fechada, entrou na varanda, limpou a garganta e bateu na porta. O Abençoado
abriu a porta.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. Então, o
Rei Pasenadi entrou na habitação prostrando-se com a cabeça aos pés do
Abençoado, cobrindo os pés do Abençoado com beijos e acariciando-os com as mãos,
pronunciando o seu nome: “Eu sou o Rei Pasenadi de Kosala, venerável senhor; Eu
sou o Rei Pasenadi de Kosala, venerável senhor.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas,
grande rei, que razão você vê para fazer uma homenagem tão extrema para este
corpo e demonstrar tamanha amizade?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10.
“Venerável senhor, eu deduzo de acordo com o Dhamma a respeito do Abençoado: ‘O
Abençoado é perfeitamente iluminado, o Dhamma é bem proclamado pelo Abençoado,
a Sangha dos discípulos do Abençoado pratica o bom caminho.’ Agora, venerável
senhor, eu vejo alguns contemplativos e brâmanes que vivem uma vida santa
limitada por dez anos, vinte anos, trinta anos, ou quarenta anos, e depois,
numa outra ocasião eu os vejo bem asseados e bem untados, com os cabelos e
barba aparados, desfrutando dos cinco elementos dos prazeres sensuais. Mas aqui
eu vejo os bhikkhus vivendo a vida santa perfeita e pura enquanto dura a vida e
a respiração. De fato, eu não vejo nenhuma outra vida santa em nenhum outro
lugar que seja tão perfeita e pura como esta. É por isso, venerável senhor, que
deduzo de acordo com o Dhamma a respeito do Abençoado: ‘O Abençoado é
perfeitamente iluminado, o Dhamma é bem proclamado pelo Abençoado, a Sangha dos
discípulos do Abençoado pratica o bom caminho.’ <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. “Outra
vez, venerável senhor, reis brigam com reis, nobres brigam com nobres, brâmanes brigam com
brâmanes, chefes de família brigam com chefes de família; mães brigam com filhos,
filhos brigam com mães, pais brigam com filhos, filhos brigam com pais; irmão briga com irmão, irmão
briga com irmã, irmã briga com irmão, amigo briga com amigo. [<a href="#N4">4</a>] <a name=R4></a>Mas
aqui vejo bhikkhus que vivem em concórdia, com apreço mútuo, sem disputas,
mesclando como leite e água, vendo um ao outro com bondade. Eu não vejo nenhuma
outra assembléia em nenhum lugar com tal concórdia. É por isso também, venerável
senhor, que deduzo de acordo com o Dhamma a respeito do Abençoado: ‘O Abençoado
é perfeitamente iluminado, o Dhamma é bem proclamado pelo Abençoado, a Sangha
dos discípulos do Abençoado pratica o bom caminho.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. “Outra
vez, venerável senhor, eu caminhei e perambulei de parque em parque e de jardim
em jardim. Lá eu vi alguns contemplativos e brâmanes magros, miseráveis, feios,
ictéricos, com as veias saltadas nos membros, de tal modo que as pessoas não
queriam nem mais olhar para eles. Eu pensei: ‘Com certeza esses veneráveis
vivem a vida santa com descontentamento, ou eles praticaram alguma ação cruel e
estão ocultando isso, tão magros, miseráveis eles são ... de tal modo que as
pessoas não queiram nem mais olhar para eles.’ Eu fui até eles e perguntei:
‘Porque os veneráveis são tão magros, miseráveis ... de tal modo que as pessoas
não querem nem mais olhar para vocês?’ A resposta foi: ‘É doença de família,
grande rei.’ Mas aqui vejo bhikkhus sorridentes e felizes, sinceramente
contentes, claramente se deliciando, com as faculdades revigoradas, vivendo em
paz, serenos, subsistindo daquilo que os outros dão, permanecendo com a mente
[contida] como um gamo selvagem. Eu pensei: ‘Com certeza estes veneráveis
percebem estados sucessivos de elevada distinção na Revelação do Abençoado,
visto que eles assim permanecem sorridentes e felizes ... com a mente [contida]
como um gamo selvagem.’ É por isso também, venerável senhor, que deduzo de
acordo com o Dhamma a respeito do Abençoado: ‘O Abençoado é perfeitamente iluminado,
o Dhamma é bem proclamado pelo Abençoado, a Sangha dos discípulos do Abençoado
pratica o bom caminho.’&nbsp;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. “Outra
vez, venerável senhor, sendo um rei nobre ungido, eu sou capaz de executar
aqueles que devem ser executados, multar aqueles que devem ser multados e de
banir aqueles que devem ser banidos. No entanto quando estou reunido com o
conselho, eles me interrompem e interferem. Embora eu diga: ‘Senhores, não me
interrompam, não interfiram quando estiver reunido com o conselho; esperem até
o fim do meu discurso,’ ainda assim eles me interrompem e interferem. Mas aqui
eu vejo que enquanto o Abençoado está ensinando o Dhamma [<a href="#N5">5</a>] <a
name=R5></a>para uma assembléia de muitas centenas de discípulos, não se ouve
nem mesmo o som de um discípulo do Abençoado tossindo ou pigarreado. Certa vez
o contemplativo Gotama ensinava o Dhamma para uma assembléia de centenas
de discípulos, então, um certo discípulo pigarreou. Por causa disso um dos seus
companheiros na vida santa o cutucou com o joelho [para indicar]: ‘Fique
quieto, venerável senhor, não faça ruído; o Abençoado, o Mestre, nos está
ensinando o Dhamma.’ Eu pensei: ‘É maravilhoso, é admirável como uma assembléia
pode ser tão bem disciplinada sem o uso da força ou armas!’ De fato, eu não
vejo nenhuma outra assembléia em nenhum outro lugar tão bem disciplinada. É por
isso também, venerável senhor, que deduzo de acordo com o Dhamma a respeito do
Abençoado: ‘O Abençoado é perfeitamente iluminado, o Dhamma é bem proclamado
pelo Abençoado, a Sangha dos discípulos do Abençoado pratica o bom caminho.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14. “Outra
vez, venerável senhor, eu vi alguns nobres instruídos, expertos, conhecedores
das doutrinas dos outros, astutos como franco-atiradores precisos; [<a
href="#N6">6</a>] <a name=R6></a>eles andam por aí, por assim dizer, demolindo
as idéias dos outros com a sua inteligência arguta. Ao ouvirem: ‘O
contemplativo Gotama irá visitar tal e tal vilarejo ou cidade,’ eles elaboram
uma questão assim: ‘Iremos até o contemplativo Gotama e faremos esta pergunta.
Se ele for perguntado assim, ele irá responder assim e portanto, iremos refutar
a sua doutrina dessa forma; ou se ele for perguntado assado, ele irá responder
assado e portanto, iremos refutar a sua doutrina dessa forma.’ <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Eles ouvem:
‘O contemplativo Gotama veio visitar tal e tal cidade ou vilarejo.’ Eles vão
até o contemplativo Gotama e o contemplativo Gotama<span style="mso-spacerun:
yes">  </span>instrui, motiva, estimula e encoraja a todos com um discurso do
Dhamma. Depois que eles foram instruídos, motivados, estimulados e encorajados
pelo contemplativo Gotama com um discurso do Dhamma, eles nem ao menos fazem a
pergunta, pois como poderiam refutar a sua doutrina? Na verdade, eles se tornam
seus discípulos. É por isso também, venerável senhor, que deduzo de acordo com
o Dhamma a respeito do Abençoado: ‘O Abençoado é perfeitamente iluminado, o
Dhamma é bem proclamado pelo Abençoado, a Sangha dos discípulos do Abençoado
pratica o bom caminho.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. “Outra
vez, venerável senhor, eu vi alguns brâmanes instruídos, expertos...<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16. “Outra vez,
venerável senhor, eu vi alguns chefes de família instruídos, expertos...<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17. “Outra
vez, venerável senhor, eu vi alguns contemplativos instruídos, expertos ... eles
nem ao menos fazem a pergunta, pois como poderiam refutar a sua doutrina? Na
verdade, eles pedem ao contemplativo Gotama permissão para seguir a vida santa
e ele lhes dá a permissão. Não muito tempo depois de ter adotado a vida santa,
permanecendo só, isolados, diligentes, ardentes e decididos e realizando por si
mesmos através do conhecimento direto, eles </span>alcançam e permanecem no
objetivo supremo da vida santa pelo qual membros de um clã deixam a vida em
família pela vida santa<span lang=PT-BR style='mso-ansi-language:PT-BR'>. Eles
dizem o seguinte: ‘Nós estávamos quase perdidos, nós tínhamos quase morrido,
pois antes reivindicávamos ser contemplativos apesar de na verdade não o
sermos; reivindicávamos ser brâmanes apesar de na verdade não o sermos;
reivindicávamos ser arahants apesar de na verdade não o sermos. Mas, agora
somos contemplativos, agora somos brâmanes, agora somos arahants.’ É por isso
também, venerável senhor, que deduzo de acordo com o Dhamma a respeito do
Abençoado: ‘O Abençoado é perfeitamente iluminado, o Dhamma é bem proclamado
pelo Abençoado, a Sangha dos discípulos do Abençoado pratica o bom caminho.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18. “Outra
vez, venerável senhor, Isidatta e Purana,[<a href="#N7">7</a>] <a name=R7></a>meus
dois inspetores, comem da minha comida e usam as minhas carruagens; eu lhes
proporciono um modo de vida e lhes trago fama. No entanto, apesar disso, eles
demonstram menos respeito para comigo do que para com o Abençoado. Certa vez,
quando parti liderando um exército e estava testando esses inspetores aconteceu de eu ter sido colocado num dormitório com muito pouco
espaço. Então esses dois inspetores depois de passarem
boa parte da noite conversando sobre o Dhamma, deitaram-se com a cabeça voltada
para a direção que eles ouviram dizer que o Abençoado estaria, e com os seus
pés voltados para mim. Eu pensei: ‘É maravilhoso, é admirável! Esses dois
inspetores comem da minha comida e usam as minhas
carruagens; eu lhes proporciono um modo de vida e lhes trago fama. No entanto,
apesar disso, eles demonstram menos respeito para comigo do que para com o
Abençoado. Com certeza estas boas pessoas percebem estados sucessivos de
elevada distinção na Revelação do Abençoado.’ É por isso também, venerável
senhor, que deduzo de acordo com o Dhamma a respeito do Abençoado: ‘O Abençoado
é perfeitamente iluminado, o Dhamma é bem proclamado pelo Abençoado, a Sangha
dos discípulos do Abençoado pratica o bom caminho.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>19. “Outra
vez, venerável senhor, o Abençoado é um nobre e eu sou um nobre, o Abençoado é
de Kosala e eu sou de Kosala, o Abençoado tem oitenta anos de idade e eu tenho
oitenta anos de idade. [<a href="#N8">8</a>] <a name=R8></a>Sendo assim, eu
penso ser apropriado fazer uma homenagem tão extrema para esse corpo e
demonstrar tamanha amizade. ”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&nbsp;20. E
agora, venerável senhor, nós partiremos. Estamos muito ocupados e temos muito
que fazer.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Agora é o
momento, grande rei, faça como julgar adequado.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o rei
Pasenadi de Kosala levantou-se do seu assento e depois de homenagear o
Abençoado, mantendo-o à sua direita, partiu.[<a href="#N9">9</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R9></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>21. Então, pouco depois dele ter partido, o Abençoado se dirigiu aos
bhikkhus da seguinte forma: “Bhikkhus, antes de se levantar do seu assento e
partir, este Rei Pasenadi expressou monumentos ao Dhamma.[<a href="#N10">10</a>]
<a name=R10></a>Aprendam os monumentos ao Dhamma, bhikkhus, conheçam bem os
monumentos ao Dhamma, recordem-se dos monumentos ao Dhamma. Os monumentos ao
Dhamma são benéficos, bhikkhus, e eles fazem parte dos fundamentos da vida
santa.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o
que disse o Abençoado. Os bhikkhus ficaram satisfeitos e contentes com as
palavras do Abençoado.<o:p></o:p></span></p>


