<p class=Normal> Então um certo brâmane foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse:</p>

<p class=Normal>"Mestre Gotama, é dito: 'Um Dhamma visível no aqui e agora, um Dhamma visível no aqui e agora.'  De que modo o Dhamma é visível no aqui e agora, com efeito imediato, que convida ao exame, que conduz para adiante, para ser experimentado pelos sábios por eles mesmos?" [<a href="#N1">1</a>]</p>

<p class=Normal><a name=R1></a> (1) "Brâmane, alguém excitado pela paixão, subjugado pela paixão, com a mente obcecada pela paixão, intenciona pela própria aflição, pela aflição dos outros, ou pela aflição de ambos, e ele experimenta sofrimento mental e angústia. Mas quando a paixão é abandonada, ele não intenciona pela própria aflição, pela aflição dos outros, ou pela aflição de ambos, e ele não experimenta sofrimento mental e angústia, É desse modo que o Dhamma é visível no aqui e agora, com efeito imediato, que convida ao exame, que conduz para adiante, para ser experimentado pelos sábios por eles mesmos. </p>

<p class=Normal> (2) "Alguém tomado pela raiva, subjugado pela raiva, com a mente obcecada pela raiva, intenciona pela própria aflição, pela aflição dos outros, ou pela aflição de ambos, e ele experimenta sofrimento mental e angústia. Mas quando a raiva é abandonada, ele não intenciona pela própria aflição, pela aflição dos outros, ou pela aflição de ambos, e ele não experimenta sofrimento mental e angústia, É desse modo que o Dhamma é visível no aqui e agora, com efeito imediato, que convida ao exame, que conduz para adiante, para ser experimentado pelos sábios por eles mesmos.</p>

<p class=Normal> (3) "Alguém deludido, subjugado pela delusão, com a mente obcecada pela delusão, intenciona pela própria aflição, pela aflição dos outros, ou pela aflição de ambos, e ele experimenta sofrimento mental e angústia. Mas quando a delusão é abandonada, ele não intenciona pela própria aflição, pela aflição dos outros, ou pela aflição de ambos, e ele não experimenta sofrimento mental e angústia, É desse modo que o Dhamma é visível no aqui e agora, com efeito imediato, que convida ao exame, que conduz para adiante, para ser experimentado pelos sábios por eles mesmos. </p>

<p class=Normal>"Magnífico, Mestre Gotama! Magnífico, Mestre Gotama! Mestre Gotama esclareceu o Dhamma de várias formas, como se tivesse colocado em pé o que estava de cabeça para baixo, revelasse o que estava escondido, mostrasse o caminho para alguém que estivesse perdido ou segurasse uma lâmpada no escuro para aqueles que possuem visão pudessem ver as formas. Eu busco refúgio no Mestre Gotama, no Dhamma e na Sangha dos bhikkhus. Que o Mestre Gotama me aceite como discípulo leigo que buscou refúgio para o resto da vida.".</p>

