<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. </span>Assim
ouvi. Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no
Parque de Anathapindika<span lang=PT-BR style='mso-ansi-language:PT-BR'>.
Agora, naquela ocasião o errante Uggahamana Samanamandikaputta estava no Parque
de<span style="mso-spacerun: yes">  </span>Mallika no salão para debates
filosóficos na plantação de Tinduka, [<a href="#N1">1</a>] <a name=R1></a>junto
com um grande grupo de discípulos errantes, uns trezentos errantes.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. O
carpinteiro Pancakanga saiu de Savatthi ao meio dia
para ver o Abençoado. Então ele pensou: “Não é o momento apropriado para ver o Abençoado,
ele ainda está em retiro. E não é o momento apropriado para ver os bhikkhus
dignos de respeito, eles ainda estão em retiro. E se eu fosse até o Parque de
Mallika, ter com o errante Uggahamana Samanamandikaputta?” E ele foi até o
Parque de Mallika.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. Agora
naquela ocasião o errante Uggahamana estava sentado com uma grande assembléia
de errantes que estavam fazendo uma grande baderna, conversando em voz alta e
aos berros sobre muitos assuntos inúteis, tal como falar sobre reis ... (igual
ao <a href="MN76.php#T4">MN 76, verso 4</a>) ... falar sobre a existência ou
não das coisas.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o
errante Uggahamana Samanamandikaputta viu o carpinteiro Pancakanga vindo à
distância. Ao vê-lo, ele silenciou a assembléia dizendo o seguinte: “Senhores,
fiquem quietos; senhores, não façam ruído. Ali vem o carpinteiro Pancakanga, um
discípulo do contemplativo Gotama, um dos discípulos leigos vestidos de branco
do contemplativo Gotama que está em Savatthi. Esses veneráveis gostam do
silêncio; eles são disciplinados no silêncio; eles recomendam o silêncio.
Talvez, se ele encontrar a nossa assembléia em silêncio, ele pensará em
juntar-se a nós.” Então os errantes ficaram em silêncio.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. O
carpinteiro Pancakanga foi até o errante Uggahamana e ambos se cumprimentaram.
Quando a conversa cortês e amigável havia terminado, ele sentou a um lado. O
errante Uggahamana então lhe disse:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5.
“Carpinteiro, quando alguém possui quatro qualidades, eu o descrevo como alguém
realizado naquilo que é benéfico, perfeito naquilo que é benéfico, tendo
alcançado a realização suprema, um asceta invencível. Quais são as quatro? Aqui
ele não pratica ações ruins com o corpo, ele não pronuncia palavras ruins, ele
não possui pensamentos ruins e ele não ganha o seu pão por meio de algum meio
de vida ruim. Quando alguém possui estas quatro qualidades, eu o descrevo como
alguém realizado naquilo que é benéfico, perfeito naquilo que é benéfico, tendo
alcançado a realização suprema, um asceta invencível.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. Então o
carpinteiro Pancakanga nem aprovou ou desaprovou as palavras do errante
Uggahamana. Sem fazer isto ou aquilo ele levantou do seu assento e partiu,
pensando: “Devo aprender o significado dessa afirmação na presença do
Abençoado.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. Então
ele foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e relatou
toda a conversa com o errante Uggahamana. Em seguida o Abençoado disse:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. “Se
fosse assim, carpinteiro, então um jovem tenro bebê deitado de costas seria
realizado naquilo que é benéfico, perfeito naquilo que é benéfico, tendo
alcançado a realização suprema, um asceta invencível, de acordo com a afirmação
do errante Uggahamana. Visto que, um jovem tenro bebê deitado de costas nem
sequer tem noção do ‘corpo,’ então como poderia ele cometer uma ação ruim além
de meras contorções? Um jovem tenro bebê deitado de costas nem sequer tem a
noção da ‘linguagem,’ então como poderia ele pronunciar palavras ruins além de
meros choramingos? Um jovem tenro bebê deitado de costas nem sequer tem a noção
de ‘intenção,’ então como poderia ele ter pensamentos ruins além da mera
sucção?<span style="mso-spacerun: yes">  </span>Um jovem tenro bebê deitado de
costas nem sequer tem<span style="mso-spacerun: yes">  </span>noção de ‘modo de
vida,’ então como poderia ele ganhar o seu pão por meio de algum meio de vida
ruim, além de ser amamentado pelo seio da sua mãe? Se fosse assim, carpinteiro,
então um jovem tenro bebê deitado de costas seria realizado naquilo que é
benéfico ... de acordo com a afirmação do errante Uggahamana.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando
alguém possui quatro qualidades, carpinteiro, eu o descrevo, não como alguém
realizado naquilo que é benéfico ou perfeito naquilo que é benéfico, ou como
alguém que alcançou a realização suprema, ou como um asceta invencível, mas
como alguém que está na mesma categoria do jovem tenro bebê deitado de costas.
Quais quatro? Aqui ele não pratica ações ruins com o corpo, ele não pronuncia
palavras ruins, ele não possui pensamentos ruins e ele não ganha o seu pão por
meio de algum meio de vida ruim. Quando alguém possui essas quatro qualidades,
eu o descrevo, não como alguém realizado ... mas como alguém que está na mesma
categoria do jovem tenro bebê deitado de costas. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. “Quando
alguém possui dez qualidades, eu o descrevo como alguém realizado naquilo que é
benéfico, perfeito naquilo que é benéfico, que alcançou a realização suprema,
um asceta invencível. [Mas antes de mais nada] Eu digo, deve ser compreendido o
seguinte: [<a href="#N2">2</a>] <a name=R2></a>‘Estes são os hábitos
prejudiciais,’ e o seguinte: ‘Hábitos prejudiciais se originam disto,’ e o
seguinte: ‘Hábitos prejudiciais cessam aqui sem deixar vestígio,’ e o seguinte:
‘Aquele que pratica desta forma está praticando o caminho para a cessação dos
hábitos prejudiciais.’ E eu digo, deve ser compreendido o seguinte: ‘Estes
são<span style="mso-spacerun: yes">  </span>os hábitos benéficos,’ e o
seguinte: ‘Hábitos benéficos se originam disto,’ e o seguinte: ‘Hábitos
benéficos cessam aqui sem deixar vestígio,’ e o seguinte: ‘Aquele que pratica
desta forma está praticando o caminho para a cessação dos hábitos benéficos.’ E
eu digo, deve ser compreendido o seguinte: ‘Estes são os pensamentos
prejudiciais,’ e o seguinte: ‘Pensamentos prejudiciais se originam disto,’ e o
seguinte: ‘Pensamentos prejudiciais cessam aqui sem deixar vestígio,’ e o
seguinte: ‘Aquele que pratica desta forma está praticando o caminho para a
cessação dos pensamentos prejudiciais.’ E eu digo, deve ser compreendido o
seguinte: ‘Estes são pensamentos benéficos,’ e o seguinte: ‘Pensamentos
benéficos se originam disto,’ e o seguinte: ‘Pensamentos benéficos cessam aqui
sem deixar vestígio,’ e o seguinte: ‘Aquele que pratica desta forma está
praticando o caminho para a cessação dos pensamentos benéficos.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. “O que
são hábitos prejudiciais? Estes são as ações prejudiciais com o corpo, ações
prejudiciais com a linguagem e meio de vida prejudicial. Estes são chamados
hábitos prejudiciais.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E de onde
se originam esses hábitos prejudiciais? A sua origem é declarada: deve ser dito
que eles se originam da mente. Qual mente? Embora a mente seja múltipla,
variada e de aspectos distintos, há a mente influenciada pela cobiça, pela raiva
e pela delusão. Os hábitos prejudiciais se originam disso.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como
esses hábitos prejudiciais cessam sem deixar vestígio? A sua cessação é
declarada: aqui um bhikkhu abandona a conduta corporal imprópria e desenvolve a
conduta corporal apropriada; ele abandona a conduta verbal imprópria e
desenvolve a conduta verbal apropriada; ele abandona a conduta mental imprópria
e desenvolve a conduta mental apropriada; ele abandona o modo de vida impróprio
e ganha o seu pão através de um modo de vida apropriado.” [<a href="#N3">3</a>]
<a name=R3></a>É assim que os hábitos prejudiciais cessam sem deixar vestígio. <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como é
que ao praticar ele pratica o caminho para a cessação dos hábitos prejudiciais?
Aqui um bhikkhu gera desejo para que não surjam estados ruins e prejudiciais
que ainda não surgiram e ele se aplica, estimula a sua energia, empenha a sua
mente e se esforça. Ele gera desejo em abandonar estados ruins e prejudiciais
que já surgiram e ele se aplica, estimula a sua energia, empenha a sua mente e
se esforça. Ele gera desejo para que surjam estados benéficos que ainda não
surgiram e ele se aplica, estimula a sua energia, empenha a sua mente e se
esforça. Ele gera desejo para a continuidade, o não desaparecimento, o
fortalecimento, o incremento e a realização através do desenvolvimento de
estados benéficos que já surgiram e ele se aplica, estimula a sua energia,
empenha a sua mente e se esforça.[<a href="#N4">4</a>] Aquele que assim pratica
está praticando o caminho para a cessação dos hábitos prejudiciais.<o:p></o:p></span></p>



<p class=Normal><a name=R4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>11. “O que são hábitos benéficos? Estes são ações benéficas com o corpo,
ações benéficas com a linguagem e meio de vida benéfico. Estes são chamados
hábitos benéficos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E de onde
se originam esses hábitos benéficos? A sua origem é declarada: deve ser dito
que eles se originam da mente. Qual mente? Embora a mente seja múltipla,
variada e de aspectos distintos, existe a mente não influenciada pela cobiça,
pela raiva e pela delusão. Os hábitos benéficos se originam disso.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como
esses hábitos benéficos cessam sem deixar vestígio? A sua cessação é
declarada:<span style="mso-spacerun: yes">  </span>aqui um bhikkhu é virtuoso,
mas ele não se identifica com a sua virtude, e ele compreende como na verdade é
a libertação da mente e a libertação através da sabedoria quando esses hábitos
benéficos cessam sem deixar vestígio. [<a href="#N5">5</a>] <a name=R5></a>É
assim que os hábitos benéficos cessam sem deixar vestígio.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como é
que ao praticar ele pratica o caminho para a cessação dos hábitos benéficos?
Aqui um bhikkhu gera desejo para que não surjam estados ruins e prejudiciais
que ainda não surgiram ... para a continuidade, o não desaparecimento, o
fortalecimento, o incremento e a realização através do desenvolvimento de
estados benéficos que já surgiram e ele se aplica, estimula a sua energia,
empenha a sua mente e se esforça. Aquele que assim pratica está praticando o
caminho para a cessação dos hábitos benéficos. [<a href="#N6">6</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>12. “O que são pensamentos prejudiciais? Estas são o pensamento do
desejo sensual, o pensamento de má vontade e o pensamento de crueldade. Estes
são chamados pensamentos prejudiciais.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E de onde
se originam esses pensamentos prejudiciais? A sua origem é declarada: deve ser
dito que elas se originam da percepção. Qual percepção? Embora a percepção seja
múltipla, variada e de aspectos distintos, há a percepção do desejo sensual, a
percepção da má vontade e a percepção da crueldade. Os pensamentos prejudiciais
se originam disso.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como
esses pensamentos prejudiciais cessam sem deixar vestígio? A sua cessação é
declarada: afastado dos prazeres sensuais, afastado das qualidades não hábeis,
um bhikkhu entra e permanece no primeiro jhana, que é caracterizado pelo
pensamento aplicado e sustentado, com o êxtase e felicidade nascidos do
afastamento. É assim que os pensamentos prejudiciais cessam sem deixar
vestígio.[<a href="#N7">7</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R7></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“E como é que ao praticar ele pratica o caminho para a cessação dos
pensamentos prejudiciais? Aqui um bhikkhu gera desejo para que não surjam
estados ruins e prejudiciais que ainda não surgiram ... para a continuidade, o
não desaparecimento, o fortalecimento, o incremento e a realização através do
desenvolvimento de estados benéficos que já surgiram e ele se aplica, estimula a
sua energia, empenha a sua mente e se esforça. Aquele que assim pratica está
praticando o caminho para a cessação dos pensamentos prejudiciais.[<a href="#N8">8</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R8></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>13. “O que são pensamentos benéficos? Estes são o pensamento de
renúncia, o pensamento de não má vontade e o pensamento de não crueldade. Estes
são chamados pensamentos benéficos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E de onde
se originam esses pensamentos benéficos? A sua origem é declarada: deve ser
dito que eles se originam da percepção. Qual percepção? Embora a percepção seja
múltipla, variada e de aspectos distintos, há a percepcão da renúncia, a
percepção da não má vontade e a percepção da não crueldade. Os pensamentos
benéficos se originam disso.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como
esses pensamentos benéficos cessam sem deixar vestígio? A sua cessação é
declarada: aqui silenciando o pensamento aplicado e sustentado, um bhikkhu
entra e permanece no segundo jhana, que é acompanhado pela autoconfiança e
unicidade da mente sem o pensamento aplicado e sustentado, com o êxtase e
felicidade nascidos da concentração. É assim que os pensamentos benéficos
cessam sem deixar vestígio.[<a href="#N9">9</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R9></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“E como é que ao praticar ele pratica o caminho para a cessação dos
pensamentos benéficos? Aqui um bhikkhu gera desejo para que não surjam estados
ruins e prejudiciais que ainda não surgiram ... para a continuidade, o não
desaparecimento, o fortalecimento, o incremento e a realização através do
desenvolvimento de estados benéficos que já surgiram e ele se aplica, estimula
a sua energia, empenha a sua mente e se esforça. Aquele que assim pratica está
praticando o caminho para a cessação dos pensamentos benéficos. [<a href="#N10">10</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R10></a><a name=T14></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>14. Agora, carpinteiro, quando alguém possui
quais dez qualidades eu o descrevo como alguém realizado naquilo que é
benéfico, perfeito naquilo que é benéfico, tendo alcançado a realização
suprema, um asceta invencível? Aqui, um bhikkhu possui o entendimento correto
daquele que está além do treinamento,[<a href="#N11">11</a>] <a name=R11></a>o
pensamento correto daquele que está além do treinamento, a linguagem correta
daquele que está além do treinamento, a ação correta daquele que está além do
treinamento, o modo de vida correto daquele que está além do treinamento, o
esforço correto daquele que está além do treinamento, a atenção plena correta
daquele que está além do treinamento, a concentração correta daquele que está
além do treinamento, o conhecimento correto daquele que está além do
treinamento e a libertação correta daquele que está além do treinamento. Quando
alguém possui essas dez qualidades eu o descrevo como alguém realizado naquilo
que é benéfico, perfeito naquilo que é benéfico, tendo alcançado a realização
suprema, um asceta invencível.”<o:p></o:p></span></p>



<p class=Normal>Isso foi o que disse o Abençoado. O <span lang=PT-BR
style='mso-ansi-language:PT-BR'>carpinteiro Pancakanga</span> ficou satisfeito
e contente com as palavras do Abençoado<span lang=PT-BR style='mso-ansi-language:
PT-BR'>.<o:p></o:p></span></p>


