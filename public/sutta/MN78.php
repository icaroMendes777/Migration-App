<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[1] MA: O parque havia sido construído pela
Rainha Mallika, a esposa do Rei Pasenadi de Kosala, embelezado com árvores floridas
e árvores frutíferas. No início apenas um salão havia sido construído, mas
depois muitos salões foram construídos. Diversos grupos de brâmanes e errantes
ali se reuniam para expor e discutir as suas doutrinas. [<a href="#R1">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[2] MA: Primeiro o Buda mostra o plano do
arahant, aquele que está além do treinamento, (isto é, mencionando as dez
qualidades), depois ele elabora um esboço aplicável ao <i>sekha</i>, o discípulo
no treinamento superior. A palavra interpretada como “hábitos” é <i>sila</i>,
que em alguns contextos pode assumir um significado mais amplo que “virtude.” [<a
href="#R2">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[3] MA explica que isto se refere ao fruto de
entrar na correnteza, pois é neste ponto que a virtude de contenção pelo
Patimokkha é realizada (e, para um Budista leigo, a observância dos Cinco
Preceitos). MA também explica os trechos subseqüentes fazendo referência aos
demais caminhos e frutos supramundanos. Embora o texto do sutta não mencione
essas realizações de modo expresso, a interpretação do comentário parece ser
justificada por conta da expressão “cessam sem deixar vestígio”, (<i>aparisesa <a
name=PVW>nirujjhanti</a></i><span style='mso-bookmark:PVW'></span>), pois é
apenas com a realização do respectivo caminho e fruto que ocorre a completa
cessação daquela contaminação em particular. A opinião do comentário é também
reforçada pela culminação de todo o discurso na figura do arahant. [<a
href="#R3">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[4] MA: Até o caminho do entrar na correnteza,
é dito que ele pratica para a cessação das contaminações; depois de ter
alcançado o fruto de entrar na correnteza, é dito que elas cessaram. [<a
