<p class=Tit3 align=center style='text-align:center'><b>Samyutta Nikaya XII.2</b></Tit3>
<p class=Tit1 align=center style='text-align:center'> Paticca-samuppada-vibhanga Sutta </Tit1>
<p class=Tit1 align=center style='text-align:center'> Análise da Origem Dependente </Tit1>

<p class=Normal align=center style='text-align:center'><span lang=PT-BR style='font-size:
