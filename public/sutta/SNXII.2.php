<p class=Normal style='margin-left:.75in'>




<p class=Normal>Em Savatthi.&quot;Bhikkhus,
eu ensinarei e analisarei para vocês a origem dependente. Ouçam e prestem muita atenção àquilo que eu
vou dizer.” – “Sim, venerável senhor,” os bhikkhus responderam. O Abençoado
disse o seguinte:</span><span lang=PT-BR style='font-size:12.0pt;mso-bidi-font-size:
10.0pt'><o:p></o:p></span></p>


<p class=Normal> &quot;O que, bhikkhus, é a origem dependente?<o:p></o:p></span></p>

<p class=Normal> Da ignorância como condição, as formações volitivas [surgem].<br>
Das formações volitivas como condição, a consciência.<br>
Da consciência como condição, a mentalidade-materialidade (nome e forma).<br>
Da mentalidade-materialidade (nome e forma) como condição, as seis bases dos sentidos.<br>
Das seis bases dos sentidos como condição, o contato.<br>
Do contato como condição, a sensação.<br>
Da sensação como condição, o desejo.<br>
Do desejo como condição, o apego.<br>
Do apego como condição, o ser/existir.<br>
<a name=nascimento></a>Do ser/existir como condição, o nascimento.<br>
<a name=envelhecimento></a>Do nascimento como condição, então o envelhecimento
e morte, tristeza, lamentação, dor, angústia e desespero surgem. Essa é a
origem de toda essa massa de sofrimento. <o:p></o:p></span></p>


<p class=Normal> &quot;Agora o que é <i style='mso-bidi-font-style:normal'>envelhecimento
e morte?</i> O envelhecimento dos seres nas diversas classes de seres, a sua
idade avançada, os dentes quebradiços, os cabelos grisalhos, a pele enrugada, o
declínio da vida, o enfraquecimento das faculdades - a isto se denomina
envelhecimento. O falecimento dos seres nas várias classes de seres, a sua
morte, a dissolução, o desaparecimento, o morrer, a finalização do tempo, a
dissolução dos agregados, o cadáver descartado<span style="mso-spacerun: yes"> 
</span>- a isto se denomina morte. <o:p></o:p></span></p>


<p class=Normal> &quot;O que é <i style='mso-bidi-font-style:normal'>nascimento? </i>O
nascimento dos seres nas várias classes de seres, o próximo nascimento, a
precipitação [em um ventre], a geração, a manifestação dos agregados, a
obtenção das bases para contato - a isto se denomina nascimento. <o:p></o:p></span></p>


<p class=Normal> &quot;O que é <i style='mso-bidi-font-style:normal'>ser/existir? </i>Existem
esses três tipos de seres: seres do reino sensual, seres do reino da matéria sutil, (ou com forma), e seres do reino imaterial, (ou sem forma) - a isto se denomina ser/existir. <o:p></o:p></span></p>


<p class=Normal> &quot;O que é<span style="mso-spacerun: yes">  </span><i
style='mso-bidi-font-style:normal'>apego? </i>Existem esses quatro tipos de
apego: apego a prazeres sensuais, apego a idéias, apego a preceitos e rituais e
apego à idéia da existência de um eu - a isto se denomina apego.<o:p></o:p></span></p>


<p class=Normal>&quot;O que é <i style='mso-bidi-font-style:normal'>desejo? </i>Existem
essas seis classes de desejo: desejo por formas, desejo por sons, desejo por
aromas, desejo por sabores, desejo por tangíveis, desejo por objetos mentais - a isto se denomina desejo.<o:p></o:p></span></p>


<p class=Normal> &quot;O que é<span style="mso-spacerun: yes">  </span><i
style='mso-bidi-font-style:normal'>sensação</i>? Existem essas seis classes de
sensações: sensações que surgem do contato no olho, sensações que surgem do
contato no ouvido, sensações que surgem do contato no nariz, sensações que
surgem do contato na língua, sensações que surgem do contato no corpo,
sensações que surgem do contato na mente - a isto se denomina sensação. <o:p></o:p></span></p>


<p class=Normal> &quot;O que é<span style="mso-spacerun: yes">  </span><i
style='mso-bidi-font-style:normal'>contato? </i>Existem essas seis classes de
contato: contato no olho, contato no ouvido, contato no nariz, contato na
língua, contato no corpo, contato na mente - a isto se denomina contato. <o:p></o:p></span></p>


<p class=Normal> &quot;O que são <i>a</i><i style='mso-bidi-font-style:normal'>s seis
bases dos sentidos? </i>Existem essas seis bases: a base do olho, a base do
ouvido, a base do nariz, a base da língua, a base do corpo, a base da mente - a isto se denomina seis bases dos sentidos.<o:p></o:p></span></p>


<p class=Normal> &quot;O que é<span style="mso-spacerun: yes">  </span><i>mentalidade-materialidade (nome e forma)</i><i
style='mso-bidi-font-style:normal'>?</i> Sensação, percepção, volição, contato
e atenção - esses são chamados de mentalidade (nome). Os quatro grandes elementos e a
forma material derivada dos quatro grandes elementos - esses são chamados de
materialidade (forma) - a isto se denomina mentalidade-materialidade (nome e forma).<o:p></o:p></span></p>


<p class=Normal> &quot;O que é<span style="mso-spacerun: yes">  </span><i
style='mso-bidi-font-style:normal'>consciência? </i>Existem essas seis classes
de consciência: consciência no olho, consciência no ouvido, consciência no
nariz, consciência na língua, consciência no corpo, consciência na mente - a isto se denomina consciência. <o:p></o:p></span></p>


<p class=Normal> &quot;O que são<span style="mso-spacerun: yes">  </span><i
style='mso-bidi-font-style:normal'>formações volitivas? </i>Existem esses três tipos de
formações: a formação corporal, a formação verbal e a formação mental - a isto se denomina formações volitivas.<o:p></o:p></span></p>


<p class=Normal> &quot;O que é <i style='mso-bidi-font-style:normal'>ignorância? </i>Não
ter o conhecimento do sofrimento, não ter o conhecimento da origem do
sofrimento, não ter o conhecimento da cessação do sofrimento, não ter o
conhecimento do caminho que conduz à cessação do sofrimento - a isto se
denomina ignorância.<o:p></o:p></span></p>


<p class=Normal> &quot;Agora, do desaparecimento e cessação sem deixar vestígios dessa
mesma ignorância cessam as formações volitivas. Da cessação das formações
volitivas cessa a consciência. Da cessação da consciência cessa a
mentalidade-materialidade (nome e forma). Da cessação da mentalidade-materialidade (nome e forma) cessam as
seis bases dos sentidos. Da cessação das seis bases dos sentidos cessa o
contato. Da cessação do contato cessa a sensação. Da cessação da sensação cessa
o desejo. Da cessação do desejo cessa o apego. Da cessação do apego cessa o
ser/existir. Da cessação do ser/existir cessa o nascimento. Da cessação do
nascimento, então envelhecimento e morte, tristeza, lamentação, dor, angústia e
desespero, tudo cessa. Essa é a cessação de toda essa massa de
sofrimento.&quot; <o:p></o:p></span></p>



