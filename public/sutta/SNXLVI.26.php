<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
desenvolvam o caminho e o meio que conduz à destruição do desejo. E qual é o
caminho e o meio que conduz à destruição do desejo? Os sete fatores da
iluminação. Quais sete? O fator da iluminação da atenção plena ... o fator da
iluminação da equanimidade.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isso
foi dito, o Venerável <a name=PVW>Udayi</a> perguntou ao Abençoado: “Venerável
senhor, como os sete fatores da iluminação são desenvolvidos e cultivados para
que conduzam à destruição do sofrimento?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Nesse
caso, Udayi, um bhikkhu desenvolve o fator da iluminação da atenção plena, que
tem como base o afastamento, desapego e cessação, que amadurece no abandono;
que é vasto, glorificado, imensurável, sem má vontade. Ao desenvolver o fator
da iluminação da atenção plena, que tem como base o afastamento ... sem má
vontade, o desejo é abandonado. Com o abandono do desejo, kamma é abandonado.
Com o abandono de kamma, o sofrimento é abandonado ... Ele desenvolve o fator
da iluminação da equanimidade, que tem como base o afastamento, desapego e
cessação, que amadurece no abandono; que é vasto, glorificado, imensurável, sem
má vontade. Ao desenvolver o fator da iluminação da atenção plena, que tem como
base o afastamento ... sem má vontade, o desejo é abandonado. Com o abandono do
desejo, kamma é abandonado. Com o abandono do kamma, o sofrimento é abandonado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
Udayi, com a destruição do desejo ocorre a destruição do kamma, com a
destruição do kamma ocorre a destruição do sofrimento.”<o:p></o:p></span></p>


