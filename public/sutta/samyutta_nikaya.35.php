style='mso-ansi-language:PT-BR'>XXXV. Salayatana-samyutta</span></b><span
lang=PT-BR style='mso-ansi-language:PT-BR'> <b> Seis bases</b></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O
Salayatana-samyutta trata das seis bases internas e externas. À primeira vista
pode parecer que as seis bases internas e externas devem ser compreendidas
simplesmente como as seis faculdades sensoriais e os seus objetos, com o termo
ayatana, base, tendo o significado de origem ou fonte. Embora muitos suttas
suportem essa suposição, a exegese Theravada compreende que os seis pares de
bases são um esquema de classificação completo capaz de acomodar todos os
fatores da existência mencionados nos Nikayas. Essa conceituação das seis bases
provavelmente teve origem no Sabba Sutta, no qual o Buda diz que os seis pares
de bases são o todo além do qual nada mais existe. Visto por este ângulo, as
seis bases internas e externas oferecem uma alternativa aos cinco agregados
como um esquema de classificação fenomenológica. A relação entre esses dois
esquemas em linhas gerais pode ser vista como semelhante à análise horizontal e
vertical de um órgão, com a análise através dos agregados correspondendo à
perspectiva horizontal e a análise através das seis bases correspondendo à
perspectiva vertical. Por exemplo, na ocasião de uma experiência visual, a
consciência no olho surge na dependência do olho e das formas; o encontro dos
três é o contato; e com o contato como condição surgem a sensação, percepção e
formações. Vendo essa experiência sob a perspectiva vertical através das bases
dos sentidos, o olho e as formas visíveis são cada um uma base separada,
respectivamente a base olho e a base forma; a consciência no olho pertence à
base mente; e o contato no olho, sensação, percepção e formações são todos
atribuídos à base objetos mentais. No entanto, embora esse tratamento das bases
encontre respaldo nos Nikayas, estes em geral apresentam os seis pares de bases
não como um esquema fenomenológico completo mas como ponto de partida para a
origem das experiências dos sentidos.</span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Da mesma
forma que com relação aos agregados, a preocupação com a classificação e
interação das bases é governada não pelo interesse em alcançar uma perfeição
teórica, mas para satisfazer as exigências práticas do caminho Budista que tem
por objetivo a libertação do sofrimento. A principal preocupação pragmática com
relação às bases é a erradicação do apego, pois tal como com os agregados, as
bases servem como solo fértil no qual o apego se enraíza e cresce. [</span><span
