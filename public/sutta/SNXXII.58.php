<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, o Tathagata, o Arahant, o Perfeitamente Iluminado, libertado
através do desapego pelo desencantamento em relação à forma, através do seu
desaparecimento e cessação, é chamado de Perfeitamente Iluminado. Um bhikkhu
libertado através da sabedoria, libertado através do desapego pelo
desencantamento em relação à forma, através do seu desaparecimento e cessação,
é chamado de libertado através da sabedoria. [<a href="#N1">1</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“O Tathagata, o Arahant, o Perfeitamente Iluminado, libertado através do
desapego pelo desencantamento em relação à sensação ... percepção ... formações volitivas
... consciência, através do seu desaparecimento e cessação, é chamado de
Perfeitamente Iluminado. Um bhikkhu libertado através da sabedoria, libertado
através do desapego pelo desencantamento em relação à sensação ... percepção
... formações volitivas ... consciência, através do seu desaparecimento e cessação, é
chamado de libertado através da sabedoria.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
bhikkhus, qual é a distinção, qual é a disparidade, qual é a diferença entre o
Tathagata, o Arahant, o Perfeitamente Iluminado, e um bhikkhu libertado através
da sabedoria?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, os nossos ensinamentos têm o Abençoado como origem, como guia e como
refúgio. Seria bom se o Abençoado pudesse explicar o significado dessas
palavras. Tendo ouvido do Abençoado, os bhikkhus o recordarão.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
ouçam e prestem muita atenção àquilo que eu vou dizer.” – “Sim, venerável
senhor,” os bhikkhus responderam. O Abençoado disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O
Tathagata, bhikkhus, o Arahant, o Perfeitamente Iluminado, é aquele que fez
surgir o caminho que não havia surgido, aquele que produziu o caminho que não
estava produzido, aquele que declarou o caminho que não estava declarado; ele
conhece o caminho, ele encontrou o caminho, ele tem habilidade no caminho. Os
seus discípulos agora seguem esse caminho e eles se tornarão possuidores dessas
qualidades no futuro.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Essa,
bhikkhus, é a distinção, é a disparidade, é a diferença entre o Tathagata, o
Arahant, o Perfeitamente Iluminado, e um bhikkhu libertado através da
sabedoria.”<o:p></o:p></span></p>




