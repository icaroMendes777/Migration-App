<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião o Abençoado estava em Ujunna, no Parque do Gamo de
Kannakatthala.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. Agora,
naquela ocasião o Rei Pasenadi de Kosala havia chegado em Ujunna para tratar de
negócios. Então ele disse para um homem:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venha, bom
homem, vá até o Abençoado e preste uma homenagem em meu nome com a sua cabeça
aos pés dele e pergunte se ele está livre de enfermidades e aflições, se está
com saúde, forte e vivendo com conforto, dizendo:<span style="mso-spacerun:
yes">  </span>‘Venerável senhor, o Rei Pasenadi de Kosala homenageia o
Abençoado com a cabeça aos seus pés e pergunta se o Abençoado está livre de
enfermidades … e vivendo com conforto.’ Então diga o seguinte: ‘Venerável
senhor, hoje o Rei Pasenadi de Kosala virá até o Abençoado depois que ele tiver
comido o seu desjejum.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
senhor,” o homem respondeu e foi até o Abençoado e ambos se cumprimentaram.
Quando a conversa cortês e amigável havia terminado, ele sentou a um lado e
relatou a sua mensagem.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. As irmãs
Soma e Sakula [<a href="#N1">1</a>] <a name=R1></a>ouviram: “Hoje o Rei
Pasenadi de Kosala irá visitar o Abençoado depois que ele tiver comido o seu
desjejum.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então,
enquanto a refeição estava sendo servida, as duas irmãs foram até o rei e
disseram: “Senhor, homenageie o Abençoado em nosso nome com a cabeça aos seus
pés e pergunte se o Abençoado está livre de enfermidades … e vivendo com
conforto, dizendo: <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>‘Venerável
senhor, as irmãs Soma and Sakula homenageiam o Abençoado com a cabeça aos seus
pés e perguntam se o Abençoado está livre de enfermidades … e vivendo com
conforto.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. Então,
quando ele terminou o desjejum, o Rei Pasenadi de Kosala foi até o Abençoado e
depois de cumprimentá-lo, sentou a um lado e relatou a mensagem das irmãs Soma e
Sakula.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas,
grande rei, as irmãs Soma e Sakula não puderam encontrar outro mensageiro?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, as irmãs Soma e Sakula ouviram: ‘Hoje o Rei Pasenadi de Kosala irá
visitar o Abençoado depois que ele tiver comido o seu desjejum.’ Então,
enquanto a refeição estava sendo servida, as duas irmãs vieram até a mim e
disseram: ‘Senhor, homenageie o Abençoado em nosso nome com a cabeça aos seus
pés e pergunte se o Abençoado está livre de enfermidades…e vivendo com
conforto.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Que as
irmãs Soma e Sakula sejam felizes, grande rei.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. Então o
Rei Pasenadi de Kosala disse para o Abençoado: “Venerável senhor, eu ouvi o
seguinte: ‘O contemplativo Gotama diz: “Não há contemplativo ou brâmane que
seja<span style="mso-spacerun: yes">  </span>onisciente e capaz de ver tudo,
que reivindique ter conhecimento e visão completos; isso não é possível.’”
Venerável senhor, aqueles que assim dizem, falam aquilo que foi dito pelo Abençoado
e não o deturpam com algo contrário aos fatos? Eles explicam de acordo com o
Dhamma de tal modo que nada que possa dar margem à censura possa de forma
legítima ser deduzido da declaração deles?<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Grande
Rei, aqueles que dizem isso não falam aquilo que foi dito por mim, mas me
deturpam com algo que não é verdadeiro e é contrário aos fatos.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. Então o
Rei Pasenadi de Kosala se dirigiu ao General Vidudabha: “General, quem
introduziu essa história no palácio?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Foi
Sanjaya, senhor, o brâmane do clã Akasa.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. Então o
Rei Pasenadi de Kosala disse para um homem: “Venha, bom homem, em meu nome diga
para Sanjaya, o brâmane do clã Akasa: ‘Venerável senhor, o Rei Pasenadi de
Kosala o chama.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
senhor,” o homem respondeu. Ele foi até Sanjaya, o brâmane do clã Akasa, e
disse: “Venerável senhor, o Rei Pasenadi de Kosala o chama.”<o:p></o:p></span></p>



<p class=Normal><a name=T8></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>8. Nesse ínterim o Rei Pasenadi de Kosala disse para o Abençoado:
“Venerável senhor, poderia outra coisa ter sido dita pelo Abençoado
referindo-se a isto e a pessoa tê-lo compreendido de forma errada? De que forma
o Abençoado se recorda ter feito essa declaração?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu recordo
ter feito essa declaração na verdade da seguinte forma, grande rei: ‘Não há
contemplativo ou brâmane que saiba tudo, que seja capaz de tudo ver, simultaneamente;
isso não é possível’” [<a href="#N2">2</a>]<o:p></o:p></span></p>


<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O que o
Abençoado disse parece ser razoável, o que o Abençoado disse parece ter suporte
na razão: ‘Não há contemplativo ou brâmane que saiba tudo, que seja capaz de
tudo ver, simultaneamente; isso não é possível.’”<o:p></o:p></span></p>



<p class=Normal><a name=T9></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>9. “Existem essas quatro castas, venerável senhor: os nobres, os
brâmanes, os comerciantes e os trabalhadores. Existe alguma distinção ou
diferença entre elas?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Existem
essas quatro castas, grande rei: os nobres, os brâmanes, os comerciantes e os
trabalhadores. Duas delas, isto é, os nobres e os brâmanes, são consideradas
como superiores visto que os homens prestam-lhes homenagem, se levantam e
prestam saudações respeitosas, e oferecem-lhes serviços com cortesia.”<o:p></o:p></span></p>



<p class=Normal><a name=T10></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>10. “Venerável senhor, eu não estava perguntando sobre esta vida atual;
eu estava perguntando sobre a vida que virá. [<a href="#N3">3</a>] <a name=R3></a><span
style="mso-spacerun: yes"> </span>Existem essas quatro castas, venerável
senhor: os nobres, os brâmanes, os comerciantes e os trabalhadores. Existe
alguma distinção ou diferença entre elas?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Grande
rei, existem estes cinco fatores para o esforço. [</span><a href="#N4"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>4</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>] <a name=R4></a>Quais cinco? Aqui um bhikkhu
tem fé, ele deposita sua fé na iluminação do Tathagata assim: ‘O Abençoado </span>é
um arahant, perfeitamente iluminado, consumado no verdadeiro conhecimento
e conduta, bem-aventurado, conhecedor dos mundos, um líder insuperável de
pessoas preparadas para serem treinadas, mestre de devas e humanos, desperto,
sublime<span lang=PT-BR style='mso-ansi-language:PT-BR'>.’ Então ele se vê
livre de enfermidades e aflições, possuindo uma boa digestão que não é nem
demasiado fria nem demasiado quente mas média e capaz de suportar a tensão do
esforço. Então ele é honesto e sincero e se mostra como na verdade ele é para o
seu mestre e os seus companheiros na vida santa. Então ele é energético no
abandono dos estados prejudiciais e no empenho pelos estados benéficos,
decidido, dedicando-se ao esforço com firmeza e perseverando no cultivo de
estados benéficos. Então ele é sábio; ele possui a  completa compreensão da origem e
cessação, que é nobre e penetrante, conduzindo à completa destruição do
sofrimento. Esses são os cinco fatores para o esforço.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Existem
essas quatro castas, grande rei: os nobres, os brâmanes, os comerciantes e os
trabalhadores. Agora se eles possuírem esses cinco fatores do esforço, isso
conduzirá ao bem-estar e felicidade deles por um longo tempo.”<o:p></o:p></span></p>


<p class=Normal><a name=T11></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>11. “Venerável senhor, existem essas quatro castas: os nobres, os
brâmanes, os comerciantes e os trabalhadores. Agora se elas possuíssem esses cinco
fatores do esforço, haveria alguma diferença entre elas com relação a isso?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
grande rei, eu digo que a diferença entre elas estaria na diversidade do seu
esforço. Suponha que houvesse dois elefantes, ou cavalos, ou bois, que estivessem
bem domados e bem disciplinados, e dois elefantes, ou cavalos, ou bois, que
estivessem indomados e indisciplinados. O que você pensa grande rei? Os dois
elefantes, ou cavalos, ou bois, que estivessem bem domados e bem disciplinados,
estando amansados eles adquiririam o comportamento dos domados, eles obteriam o
grau dos domados?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
venerável senhor.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E os dois
elefantes, ou cavalos, ou bois, que estivessem indomados e indisciplinados,
estando indomados eles adquiririam o comportamento dos domados, eles obteriam o
grau dos domados, igual aos dois elefantes, ou cavalos, ou bois que estavam bem
domados e bem disciplinados?” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
venerável senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Assim
também, grande rei, não é possível que aquilo que pode ser alcançado por alguém
que tem fé, que está livre de enfermidades, que é honesto e sincero, que é
energético e que é sábio, possa ser alcançado por alguém que não tem fé, que é
enfermo, que é dissimulador e trapaçeiro, que é preguiçoso, e que não é sábio.”<o:p></o:p></span></p>



<p class=Normal><a name=T12></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>12. “O que o Abençoado disse parece ser razoável, o que o Abençoado
disse parece ter suporte na razão.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Existem
essas quatro castas, venerável senhor: os nobres, os brâmanes, os comerciantes
e os trabalhadores. Agora, se elas possuíssem esses cinco fatores do esforço e
se o seu esforço fosse correto, haveria alguma diferença entre elas com relação
a isso?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
grande rei, com relação a isso eu digo que entre elas não há diferença, isto é,
entre a libertação de uma e a libertação das outras. Suponha que um homem
tomasse a madeira seca de uma árvore saka, acendesse um fogo e produzisse
calor; e depois um outro homem tomasse a madeira seca de uma árvore sala,
acendesse um fogo e produzisse calor; e depois um outro homem tomasse a madeira
seca de uma mangueira, acendesse um fogo e produzisse calor; e depois um outro
homem tomasse a madeira seca de uma figueira, acendesse um fogo e produzisse
calor. O que você pensa, grande rei? Haveria alguma diferença no fogo acendido
com esses diferentes tipos de madeira, isto é, entre a chama de uma e a chama
das outras, ou entre a cor de uma e a cor das outras, ou entre a luminosidade
de uma e a luminosidade das outras?” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
venerável senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Assim
também, grande rei, quando um fogo é acendido através da energia, queima através
do esforço, não há, eu digo, diferença, isto é, entre a libertação de uma e a
libertação das outras.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. “O que
o Abençoado disse parece ser razoável, o que o Abençoado disse parece ter
suporte na razão. Mas, venerável senhor, como é isso: existem devas?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Porque
você pergunta isso, grande rei?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, eu estava perguntando se os devas retornam a este estado [humano]
ou<span style="mso-spacerun: yes">  </span>não retornam.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Grande
rei, aqueles devas que ainda estão sujeitos à aflição retornam a este estado [humano],
aqueles devas que não estão mais sujeitos à aflição não retornam a este estado
humano.” [</span><a href="#N5"><span lang=PT-BR style='mso-ansi-language:PT-BR'>5</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=R5></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>14. Quando isso foi dito, o General Vidudabha perguntou para o
Abençoado: “Venerável senhor, aqueles devas que ainda estão sujeitos à aflição
e que retornam a este estado [humano] são capazes de demitir ou banir daquele
lugar aqueles devas que não estão mais sujeitos à aflição e que não retornam a
este estado [humano]?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o
venerável Ananda pensou: “Esse General Vidudabha é o filho do Rei Pasenadi de
Kosala, e eu sou o filho do Abençoado. Este é o momento em que um filho deve
falar ao outro.” Ele disse para o General Vidudabha: “General, eu lhe farei uma
pergunta em retorno. Responda como quiser, General, o que você acha? Há toda
essa extensão do reino do Rei Pasenadi de Kosala, onde ele exerce liderança e
soberania; agora, o Rei Pasenadi de Kosala pode demitir ou banir desse lugar
qualquer contemplativo ou brâmane, sem tomar em conta se esse contemplativo ou
brâmane possui mérito ou não, e se ele vive a vida santa ou não?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele pode
fazer isso, senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O que você
pensa, general? Há toda essa extensão que não faz parte do reino do Rei
Pasenadi de Kosala, onde ele não exerce liderança e soberania; agora, o Rei
Pasenadi de Kosala pode demitir ou banir desse lugar qualquer contemplativo ou
brâmane, sem tomar em conta se esse contemplativo ou brâmane possui mérito ou
não, e se ele vive a vida santa ou não?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele não
pode fazer isso, senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“General, o
que você pensa? Você ouviu acerca dos devas do Trinta e três?” “Sim, senhor,
eu ouvi acerca deles. E o Rei Pasenadi de Kosala também ouviu acerca deles.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“General, o
que você pensa? O Rei Pasenadi de Kosala pode demitir os devas do Trinta e
três ou baní-los daquele lugar?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Senhor, o
Rei Pasenadi de Kosala não é nem capaz de ver os devas do Trinta e três,
então como poderia ele demiti-los ou baní-los daquele lugar?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Assim
também, general, aqueles devas que ainda estão sujeitos à aflição e que
retornam a este estado [humano] não são nem capazes de ver os devas que não
estão mais sujeitos à aflição e que não retornam a este estado [humano]; então
como poderiam eles demiti-los ou baní-los daquele lugar?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. Então o
Rei Pasenadi de Kosala perguntou para o Abençoado: “Venerável senhor, qual é o
nome desse bhikkhu?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O nome
dele é Ananda, grande rei.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ananda
[alegria] ele de fato é, venerável senhor, e Ananda ele aparenta ser. O que o
venerável Ananda disse parece ser razoável, o que ele disse parece ter suporte
na razão. Mas, venerável senhor, como é isso: existem Brahmas?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Porque
você pergunta isso, grande rei?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, eu estava perguntando se os Brahmas retornam a este estado [humano] ou
se não retornam.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Grande rei,
aqueles Brahmas que ainda estão sujeitos à aflição retornam a este estado
[humano], aqueles Brahmas que não estão mais sujeitos à aflição não retornam a
este estado humano.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16. Então
um homem anunciou para o Rei Pasenadi de Kosala: “Grande rei, Sanjaya, o
brâmane do clã Akasa chegou.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O Rei
Pasenadi de Kosala perguntou a Sanjaya, o brâmane do clã Akasa: “Brâmane, quem
introduziu essa história no palácio?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Senhor,
foi o General Vidudabha.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O General
Vidudabha disse: “Senhor foi Sanjaya, o brâmane do clã Akasa.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17. Então
um homem anunciou para o Rei Pasenadi de Kosala: “Senhor, está na hora de
partir.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O Rei
Pasenadi de Kosala disse para o Abençoado: “Venerável senhor, perguntamos ao
Abençoado acerca da onisciência e o Abençoado respondeu acerca da onisciência;
nós aprovamos e aceitamos a resposta e assim estamos satisfeitos. Perguntamos
ao Abençoado acerca da purificação das quatro castas e o Abençoado respondeu
acerca da purificação das quatro castas; nós aprovamos e aceitamos a resposta e
assim estamos satisfeitos. Perguntamos ao Abençoado acerca dos devas e o
Abençoado respondeu acerca dos devas; nós aprovamos e aceitamos a resposta e
assim estamos satisfeitos. Perguntamos ao Abençoado acerca dos Brahmas e o
Abençoado respondeu acerca dos Brahmas; nós aprovamos e aceitamos a resposta e
assim estamos satisfeitos. Qualquer coisa que perguntamos ao Abençoado, isso o
Abençoado respondeu; nós aprovamos e aceitamos as respostas e assim estamos
satisfeitos. E agora, venerável senhor, nós partiremos. Estamos ocupados e
temos muito o que fazer”.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Agora é o
momento, grande rei, faça como julgar adequado.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18. Então o
Rei Pasenadi de Kosala, tendo ficado satisfeito e contente com as palavras do
Abençoado, levantou-se do seu assento e depois de homenagear o Abençoado,
mantendo-o à sua direita, partiu.<o:p></o:p></span></p>




