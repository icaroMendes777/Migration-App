<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião, o Abençoado estava em Savatthi no Bosque de Jeta, no
Parque de Anathapindika.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. Então,
Mahapajapati Gotami juntamente com quinhentas bhikkhunis foi até o Abençoado.
Depois de cumprimentá-lo, ela ficou em pé a um lado e disse: “Venerável senhor,
que o Abençoado instrua as bhikkhunis, que o Abençoado ensine as bhikkhunis,
que o Abençoado faça um discurso do Dhamma para as bhikkhunis.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. Agora,
naquela ocasião os bhikkhus sêniores estavam se revezando na instrução das
bhikkhunis, mas o venerável Nandaka não queria instruí-las quando chegou a sua
vez.[<a href="#N1">1</a>] <a name=R1></a>Então, o Abençoado se dirigiu ao
venerável Ananda: “Ananda, de quem é a vez, hoje, de instruir as bhikkhunis?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, é a vez do venerável Nandaka de instruir as bhikkhunis, mas ele não
quer instruí-las, muito embora seja a vez dele.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. Então, o
Abençoado se dirigiu ao venerável Nandaka: “Instrua as bhikkhunis, Nandaka.
Instrua as bhikkhunis, Nandaka. Faça um discurso do Dhamma para as bhikkhunis,
brâmane.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
venerável senhor,” o venerável Nandaka respondeu. Então, ao amanhecer, o
venerável Nandaka se vestiu e tomando a tigela e o manto externo foi para
Savatthi para esmolar alimentos. Depois de haver esmolado em Savatthi e de
haver retornado, após a refeição, ele foi com um acompanhante até o Parque
Rajaka. As bhikkhunis viram o venerável Nandaka vindo à distância e prepararam
um assento e água para os pés. O venerável Nandaka sentou no assento que havia
sido preparado e lavou os pés. As bhikkhunis o cumprimentaram e sentaram a um
lado. E quando elas sentaram o venerável Nandaka disse para as bhikkhunis:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. “Irmãs,
esta palestra será dada em forma de perguntas e respostas. Quando vocês
compreenderem devem dizer: ‘Nós compreendemos’, quando vocês não compreenderem
devem dizer: ‘Nós não compreendemos’; quando vocês tiverem dúvidas ou
perplexidades vocês devem me perguntar: ‘Como é isso, venerável senhor? Qual é
o significado disso?’”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, nós estamos satisfeitas e contentes pelo mestre Nandaka ter-nos
convidado dessa forma.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. “Irmãs,
o que vocês pensam? O olho é permanente ou impermanente?” – “Impermanente,
venerável senhor” – “Aquilo que é impermanente é sofrimento ou felicidade?” –
“Sofrimento, venerável senhor.” – “Aquilo que é impermanente, sofrimento e sujeito
à mudança, é adequado que seja considerado assim: ‘Isso é meu, isso sou eu,
isso é o meu eu’?” – “Não, venerável senhor.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Irmãs, o
que vocês pensam?<span style="mso-spacerun: yes">  </span>O ouvido ... o nariz
... a língua ... o corpo ... a mente é permanente ou impermanente?” – “Impermanente,
venerável senhor” – “Aquilo que é impermanente é sofrimento ou felicidade?” –
“Sofrimento, venerável senhor.” – “Aquilo que é impermanente, sofrimento e
sujeito à mudança, é adequado que seja considerado assim: ‘Isso é meu, isso sou
eu, isso é o meu eu’?” – “Não, venerável senhor. Por que isso? Porque, venerável
senhor, nós já vimos isso com clareza como na verdade é, com correta sabedoria
assim: ‘Essas seis bases internas são impermanentes’” [<a href="#N2">2</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Muito bem, irmãs! Assim é um nobre discípulo que vê como na verdade é,
com correta sabedoria.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. “Irmãs,
o que vocês pensam? As formas ... sons ... aromas ... sabores ... tangíveis ...
objetos mentais são permanentes ou impermanentes?” – “Impermanentes, venerável
senhor” – “Aquilo que é impermanente é sofrimento ou felicidade?” –
“Sofrimento, venerável senhor.” – “Aquilo que é impermanente, sofrimento e
sujeito à mudança, é adequado que seja considerado assim: ‘Isso é meu, isso sou
eu, isso é o meu eu’?” – “Não, venerável senhor. Por que isso? Porque, venerável
senhor, nós já vimos isso com clareza como na verdade é, com correta sabedoria
assim: ‘Essas seis bases externas são impermanentes’” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Muito bem,
irmãs! Assim é um nobre discípulo que vê como na verdade é, com correta
sabedoria. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. “Irmãs,
o que vocês pensam? A consciência no olho ... a<span style="mso-spacerun:
yes">  </span>consciência no ouvido ... a consciência no nariz ... a
consciência na língua ... a consciência no corpo ... a consciência na mente é
permanente ou impermanente?” – “Impermanente, venerável senhor” – “Aquilo que é
impermanente é sofrimento ou felicidade?” – “Sofrimento, venerável senhor.” –
“Aquilo que é impermanente, sofrimento e sujeito à mudança, é adequado que seja
considerado assim: ‘Isso é meu, isso sou eu, isso é o meu eu’?” – “Não, venerável
senhor. Por que isso? Porque, venerável senhor, nós já vimos isso com clareza,
como na verdade é, com correta sabedoria assim: ‘Essas seis classes de
consciência são impermanentes’” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Muito bem,
irmãs! Assim é um nobre discípulo que vê como na verdade é, com correta
sabedoria.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. “Irmãs,
suponham que uma lamparina de azeite estivesse queimando: o seu azeite é
impermanente e sujeito à mudança, o seu pavio é impermanente e sujeito à
mudança, a sua chama é impermanente e sujeita à mudança e o seu brilho é
impermanente e sujeito à mudança. Agora, alguém que fale corretamente diria
isto: ‘Enquanto esta lamparina queima, o seu azeite, pavio e chama são
impermanentes e sujeitos à mudança, mas o seu brilho é permanente,
interminável, eterno e não está sujeito à mudança’?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
venerável senhor. Por que isso? Porque venerável senhor, enquanto aquela
lamparina queima, o seu azeite, pavio e chama são impermanentes e sujeitos à
mudança e assim o seu brilho também tem que ser impermanente e sujeito à
mudança.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Da mesma
forma, irmãs, alguém que fale corretamente diria isto: ‘Estas seis bases
internas são impermanentes e sujeitas à mudança, mas a sensação prazerosa,
dolorosa ou nem dolorosa, nem prazerosa que é experimentada na dependência
dessas seis bases internas é permanente, interminável, eterna e não está
sujeita à mudança’?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
venerável senhor. Por que isso?<span style="mso-spacerun: yes">  </span>Porque
cada sensação surge na dependência da sua condição correspondente [<a href="#N3">3</a>]
<a name=R3></a>e com a cessação da sua condição correspondente, a sensação
cessa.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Muito bem,
irmãs! Assim é um nobre discípulo que vê como na verdade é, com sabedoria
adequada.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. “Irmãs,
suponham uma grande árvore plantada possuidora de cerne: a sua raiz é
impermanente e sujeita à mudança, o seu tronco é impermanente e sujeito à
mudança, os seus galhos e folhas são impermanentes e sujeitos à mudança, e a
sua sombra é impermanente e sujeita à mudança. Agora, alguém que fale
corretamente diria isto: ‘A raiz, o tronco, os galhos e folhas desta grande
árvore plantada possuidora de cerne são impermanentes e sujeitos à mudança, mas
a sua sombra é permanente, interminável, eterna, não sujeita à mudança’?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
venerável senhor. Por que isso? Porque venerável senhor, a raiz, o tronco, os
galhos e folhas desta grande árvore plantada possuidora de cerne são
impermanentes e sujeitos à mudança, assim a sua sombra também tem que ser
impermanente e sujeita à mudança.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Da mesma
forma, irmãs, alguém que fale corretamente diria isto: ‘Estas seis bases
externas são impermanentes e sujeitas à mudança, mas a sensação prazerosa,
dolorosa ou nem dolorosa, nem prazerosa que é experimentada na dependência
dessas seis bases externas é permanente, interminável, eterna, e não está sujeita
à mudança’?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
venerável senhor. Por que isso?<span style="mso-spacerun: yes">  </span>Porque
cada sensação surge na dependência da sua condição correspondente e com a
cessação da sua condição correspondente, a sensação cessa.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Muito bem,
irmãs! Assim é um nobre discípulo que vê como na verdade é, com sabedoria
adequada.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. “Irmãs,
suponham que um hábil açougueiro ou o seu aprendiz matasse uma vaca e a
descarnasse com uma faca afiada de açougueiro. Sem danificar a massa interna de
carne e sem danificar o couro externo, ele cortaria, separaria e trincharia os
tendões internos, nervos e ligamentos com a faca afiada de açougueiro. Então,
depois de cortar, separar e trinchar tudo isso, ele removeria o couro externo e
cobriria novamente a vaca com aquele mesmo couro. Ele falaria corretamente se
dissesse: ‘Esta vaca está unida a este couro da mesma forma que antes’?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
venerável senhor. Por que isso? Porque se aquele hábil açougueiro ou o seu
aprendiz matasse uma vaca ... e cortasse, separasse e trinchasse tudo aquilo,
muito embora ele cubra a vaca novamente com aquele mesmo couro e diga: ‘Esta
vaca está unida a este couro da mesma forma que antes,’ aquela vaca está
desunida daquele couro.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. “Irmãs,
eu citei este símile para transmitir uma idéia. A idéia é a seguinte: ‘A massa
interna de carne’ é um termo para as seis bases internas. ‘O couro externo’ é
um termo para as seis bases externas. ‘Os tendões internos, nervos e
ligamentos’ é um termo para o prazer e a cobiça. ‘A faca afiada do açougueiro’
é um termo para a nobre sabedoria – a nobre sabedoria que corta, separa e
trincha as contaminações internas, os grilhões e laços.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. “Irmãs,
há esses sete fatores da iluminação[<a href="#N4">4</a>] <a name=R4></a>e um bhikkhu que
através do seu desenvolvimento e cultivo os realize por si mesmo com o conhecimento direto, aqui e
agora, entra e permanece na libertação da mente e libertação através da sabedoria que
são imaculadas com a destruição das impurezas. Quais são os sete? Aqui, irmãs,
um bhikkhu desenvolve o fator da iluminação da atenção plena, que tem como base
o afastamento, desapego e cessação que amadurece no abandono. Ele desenvolve o
fator da iluminação da investigação dos fenômenos<i> ... </i>Ele desenvolve o
fator da iluminação da energia ... Ele desenvolve o fator da iluminação do
êxtase ... Ele desenvolve o fator da iluminação da tranqüilidade ... Ele
desenvolve o fator da iluminação da concentração<i> ... </i>Ele desenvolve o
fator da iluminação da equanimidade, que tem como base o afastamento, desapego
e cessação que amadurece no abandono. Esses são os sete fatores da iluminação e
através do seu desenvolvimento e cultivo um bhikkhu, que realize por si mesmo
com o conhecimento direto aqui e agora, entra e permanece na libertação da
mente e libertação através da sabedoria que são imaculadas com a destruição das impurezas.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14. Quando
o venerável Nandaka instruiu as bhikkhunis dessa forma, ele as dispensou
dizendo: “Vão, irmãs, é hora.” Então as bhikkhunis, satisfeitas e contentes com
as palavras do venerável Nandaka, levantaram dos seus assentos e depois de homenagear
o venerável Nandaka, mantendo-o à sua direita, partiram. Elas foram até o
Abençoado e depois de cumprimentá-lo ficaram em pé a um lado. O Abençoado
disse: “Vão, irmãs, é hora.” Então as bhikkhunis homenagearam o Abençoado e
mantendo-o à sua direita, partiram.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. Pouco
tempo depois delas terem partido o Abençoado se dirigiu aos bhikkhus:
“Bhikkhus, da mesma forma como no décimo quarto dia do Uposatha as pessoas não
têm dúvidas ou perplexidades quanto à Lua estar cheia ou não, visto que nessa
ocasião a Lua ainda não está cheia, assim também aquelas bhikkhunis ficaram
satisfeitas com o ensinamento do Dhamma de Nandaka, mas a intenção delas ainda
não foi satisfeita.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16-26.
Então, o Abençoado se dirigiu ao venerável Nandaka: “Bem, neste caso, Nandaka,
você também deveria amanhã instruir essas bhikkhunis exatamente da mesma
forma.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
venerável senhor,” o venerável Nandaka respondeu. Então, ao amanhecer, o
venerável Nandaka se vestiu ... (repetir os versos 4-14 até ) ... Então as
bhikkhunis homenagearam o Abençoado e mantendo-o à sua direita, partiram.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>27. Pouco
tempo depois delas terem partido o Abençoado se dirigiu aos bhikkhus:
“Bhikkhus, da mesma forma como no décimo quinto dia do Uposatha as pessoas não
têm dúvidas ou perplexidades quanto à Lua estar cheia ou não, visto que nessa
ocasião a Lua está claramente cheia, assim também aquelas bhikkhunis ficaram
satisfeitas com o ensinamento do Dhamma de Nandaka e a intenção delas foi
satisfeita. Bhikkhus, mesmo a menos avançada dentre essas quinhentas bhikkhunis
entrou na correnteza não mais destinada aos mundos inferiores, com o destino
fixo, ela tem a iluminação como destino. [<a href="#N5">5</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R5></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Isso foi o que disse o Abençoado. Os bhikkhus ficaram satisfeitos e contentes
com as palavras do Abençoado.<o:p></o:p></span></p>


