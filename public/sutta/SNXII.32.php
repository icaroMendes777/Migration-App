<p class=Normal> Em Savatthi. </p>

<p class=Normal>Então o bhikkhu Kalara, o Khattiya, foi até o venerável Sariputta e ambos se cumprimentaram. Depois que a conversa cortes e amigável havia terminado ele sentou a um lado e disse: "Amigo Sariputta, o bhikkhu Moliyaphagguna abandonou o treinamento e voltou para a vida inferior." </p>

<p class=Normal>"Então com certeza esse venerável não encontrou consolo neste Dhamma e Disciplina." </p>

<p class=Normal>"Então o venerável Sariputta obteve consolo neste Dhamma e Disciplina?" </p>

<p class=Normal>"Eu não tenho incertezas, amigo." </p>

<p class=Normal>"Mas e quanto ao futuro, amigo?" </p>

<p class=Normal>"Eu não tenho dúvidas, amigo." [<a href="#N1">1</a>]</p>

<p class=Normal><a name=R1></a>Então o bhikkhu Kalara, o Khattiya, foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse: "Venerável senhor, o venerável Sariputta declarou o conhecimento supremo assim: 'Eu compreendo: O nascimento foi destruído, a vida santa foi vivida, o que devia ser feito foi feito, não há mais vir a ser a nenhum estado.'"</p>

<p class=Normal>Então o Abençoado se dirigiu a um certo bhikkhu desta forma: “Venha, bhikkhu, diga a Sariputta em meu nome que o Mestre o chama.” “Sim, venerável senhor,” ele respondeu e foi até o venerável Sariputta e lhe disse: “O Mestre o chama, amigo Sariputta.” </p>

<p class=Normal> “Sim, Amigo,” o venerável Sariputta respondeu, e foi até o Abençoado e após cumprimentá-lo sentou a um lado. O Abençoado então lhe perguntou: “Sariputta, é verdade que você declarou o conhecimento supremo assim: 'Eu compreendo: O nascimento foi destruído, a vida santa foi vivida, o que devia ser feito foi feito, não há mais vir a ser a nenhum estado.'?" </p>

<p class=Normal>"Venerável senhor, eu não respondi empregando esses termos e frases." </p>

<p class=Normal>"Sariputta, de qualquer forma que um membro de um clã declare o conhecimento supremo, aquilo que ele declarou deve assim ser entendido."

<p class=Normal>"Venerável senhor, eu não disse isto: 'Venerável senhor, eu não respondi empregando esses termos e frases'?'</p>

<p class=Normal>"Sariputta, se alguém lhe perguntasse: 'Amigo Sariputta, como foi que você compreendeu, como você viu, para declarar o conhecimento supremo assim: 'Eu compreendo: O nascimento foi destruído, a vida santa foi vivida, o que devia ser feito foi feito, não há mais vir a ser a nenhum estado?' - sendo assim perguntado, como você responderia?" </p>

<p class=Normal>"Se fosse assim perguntado, venerável senhor, eu responderia assim: 'Com a destruição da fonte da qual o nascimento se origina, eu compreendi: "Quando a causa é destruída, o efeito é destruído." Tendo compreendido isso, eu compreendo: 'O nascimento foi destruído, a vida santa foi vivida, o que devia ser feito foi feito, não há mais vir a ser a nenhum estado' - sendo assim perguntado, eu responderia dessa forma." </p>

<p class=Normal>"Mas, Sariputta, se lhe perguntassem: 'Mas, amigo Sariputta, qual é a fonte do nascimento, qual é a sua origem, do que ele nasce e é produzido?'  - sendo assim perguntado, como você responderia?" </p>

<p class=Normal>"Se fosse assim perguntado, venerável senhor, eu responderia assim: 'Nascimento amigos, tem o ser/existir como fonte, ser/existir como origem, é criado e produzido pelo ser/existir' - sendo assim perguntado, eu responderia dessa forma."

<p class=Normal>"Mas, Sariputta, se lhe perguntassem: 'Mas, amigo Sariputta, qual é a fonte do ser/existir, ... sendo assim perguntado, como você responderia?" </p>

<p class=Normal>"Se fosse assim perguntado, venerável senhor, eu responderia assim: Ser/existir amigos, tem o apego como fonte ... '"</p>

<p class=Normal>"Mas, Sariputta, se lhe perguntassem: 'Mas, amigo Sariputta, qual é a fonte do apego, ...? qual é a fonte do desejo, qual é a sua origem, do que ele nasce e é produzido?'  -  sendo assim perguntado, como você responderia?" </p>

<p class=Normal>"Se fosse assim perguntado, venerável senhor, eu responderia assim: Desejo amigos, tem a sensação como fonte, sensação como origem, é criado e produzido pela sensação' - sendo assim perguntado, eu responderia dessa forma." </p>

<p class=Normal>"Mas, Sariputta, se lhe perguntassem: 'Mas, amigo Sariputta, como você sabe, como você vê, que o deleite com as sensações não está mais presente em você? - sendo assim perguntado, como você responderia?" </p>

<p class=Normal>"Se fosse assim perguntado, venerável senhor, eu responderia assim: 'Amigos, há esses três tipos de sensações. Quais três? A sensação prazerosa, a sensação dolorosa, a sensação nem prazerosa nem dolorosa. Essas três sensações, amigo, são impermanentes. Tudo que é impermanente é sofrimento. Quando isso foi compreendido, o deleite com as sensações não estava mais presente em mim' - sendo assim perguntado, eu responderia dessa forma." </p>

<p class=Normal><a name=R2></a>"Muito bem, Sariputta, muito bem! Esta é uma outra forma de explicar o mesmo ponto em resumo: 'Tudo aquilo que é sentido faz parte do sofrimento.' [<a href="#N2">2</a>] Mas, Sariputta, se lhe perguntassem: 'Mas, amigo Sariputta, através de qual libertação você declarou o conhecimento supremo assim: "Eu compreendo: o nascimento foi destruído, a vida santa foi vivida, o que devia ser feito foi feito, não há mais vir a ser a nenhum estado"?' - sendo assim perguntado, como você responderia?" </p>

<p class=Normal>"Se fosse assim perguntado, venerável senhor, eu responderia assim: 'Amigos, através de uma libertação interna, através da destruição de todo apego, eu permaneço com atenção plena de tal forma que as impurezas não se manifestam em mim e eu não desprezo a mim mesmo' - sendo assim perguntado, eu responderia dessa forma." </p>

<p class=Normal>"Muito bem, Sariputta, muito bem! Esta é uma outra forma de explicar o mesmo ponto em resumo: 'Eu não tenho incertezas com relação às impurezas explicadas pelo Contemplativo; eu não tenho dúvidas que elas foram abandonadas por mim.'"</p>

<p class=Normal>Isso foi o que o Abençoado disse. Tendo dito isso ele levantou do seu assento e foi para a sua moradia.</p>

