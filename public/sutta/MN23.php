<p class=Normal style='margin-left:1.0in'>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. </span>Assim
ouvi. Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no
Parque de Anathapindika<span lang=PT-BR style='mso-ansi-language:PT-BR'>.
Agora, naquela ocasião o venerável Kumara Kassapa estava no Bosque dos Cegos. [<a
href="#N1">1</a>]<o:p></o:p></span></p>


<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então,
quando a noite já estava bem avançada, um certo deva com belíssima aparência
que iluminou toda a área do Bosque dos Cegos se aproximou do venerável Kumara
Kassapa. Ficando em pé a um lado, o deva disse: [<a href="#N2">2</a>]<o:p></o:p></span></p>


<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2. “Bhikkhu, bhikkhu, este formigueiro fumega durante a noite e arde
durante o dia. [<a href="#N3">3</a>]<o:p></o:p></span></p>


<p class=Normal><a name=R3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Assim disse o brâmane: ‘Escave com a faca, você, sábio.’ Escavando com
a faca, o sábio viu uma barra: ‘Uma barra, Oh venerável senhor.’<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Assim
disse o brâmane:<span style="mso-spacerun: yes">  </span>‘Jogue fora a barra;
escave com a faca, você, sábio.’ Escavando com a faca, o sábio viu um sapo: ‘Um
sapo, Oh venerável senhor.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Assim
disse o brâmane:<span style="mso-spacerun: yes">  </span>‘Jogue fora o sapo;
escave com a faca, você, sábio.’ Escavando com a faca, o sábio viu um garfo:
‘Um garfo, Oh venerável senhor.’<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Assim
disse o brâmane:<span style="mso-spacerun: yes">  </span>‘Jogue fora o garfo;
escave com a faca, você, sábio.’ Escavando com a faca, o sábio viu uma peneira:
‘Uma peneira, Oh venerável senhor.’<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Assim
disse o brâmane:<span style="mso-spacerun: yes">  </span>‘Jogue fora a peneira;
escave com a faca, você, sábio.’ Escavando com a faca, o sábio viu um jabuti:
‘Um jabuti, Oh venerável senhor.’<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Assim
disse o brâmane:<span style="mso-spacerun: yes">  </span>‘Jogue fora o jabuti;
escave com a faca, você, sábio.’ Escavando com a faca, o sábio viu um machado e
um cepo: ‘Um machado e um cepo, Oh venerável senhor.’<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Assim
disse o brâmane:<span style="mso-spacerun: yes">  </span>‘Jogue fora o machado
e o cepo; escave com a faca, você, sábio.’ Escavando com a faca, o sábio viu um
pedaço de carne: ‘Um pedaço de carne, Oh venerável senhor.’ <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Assim
disse o brâmane:<span style="mso-spacerun: yes">  </span>‘Jogue fora o pedaço
de carne; escave com a faca, você, sábio.’ Escavando com a faca, o sábio viu
uma serpente Naga: ‘Uma serpente Naga, Oh venerável senhor.’<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Assim
disse o brâmane: ‘Deixe a serpente Naga; não cause dano à serpente Naga; honre
a serpente Naga.’<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhu,
você deveria ir até o Abençoado e perguntar-lhe o significado desta charada.
Aquilo que o Abençoado<span style="mso-spacerun: yes">  </span>disser, assim
você deverá se recordar. Bhikkhu, outro que não seja o Tathagata, ou um
discípulo do Tathagata, ou alguém que tenha aprendido com eles, eu não vejo
ninguém neste mundo </span>com os seus devas, maras e brahmas, esta população
com seus contemplativos e brâmanes, seus príncipes e povo<span lang=PT-BR
style='mso-ansi-language:PT-BR'>, cuja explicação dessa charada possa
satisfazer a mente.”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o
que o deva disse e em seguida desapareceu de vez.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. Então,
ao amanhecer, o venerável Kumara Kassapa foi até o Abençoado e depois de
cumprimentá-lo<span style="mso-spacerun: yes">  </span>sentou a um lado e
relatou tudo que havia ocorrido. E então perguntou: “Venerável senhor, o que é
o formigueiro, o que fumega durante a noite, o que arde durante o dia? Quem é o
brâmane, quem é o sábio? O que é a faca, o que é a escavação, o que é a barra,
o que é o sapo, o que é o garfo, o que é a peneira, o que é o jabuti, o que é o
machado e o cepo, o que é o pedaço de carne, o que é a serpente Naga?”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4.
“Bhikkhu, o formigueiro simboliza este corpo, este corpo feito de forma
material, consistindo dos quatro grandes elementos, procriado por uma mãe e um
pai, construído à base de arroz cozido e mingau,[<a href="#N4">4</a>] <a
name=R4></a>está sujeito à impermanência, a ser gasto e pulverizado, à
dissolução e desintegração.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aquilo que
a pessoa pensa e reflete durante a noite baseado nas ações durante o dia é o
‘fumegar durante a noite.’<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“As ações
que a pessoa realiza durante o dia através do corpo, linguagem e mente depois de
pensar e refletir durante a noite é o ‘arder durante o dia.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O brâmane
simboliza o Tathagata, um arahant, perfeitamente iluminado. O sábio simboliza o
bhikkhu no treinamento superior. A faca simboliza a nobre sabedoria. A
escavação simboliza estimular a energia.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“A barra
simboliza a ignorância. [<a href="#N5">5</a>] ‘<a name=R5></a>Jogue fora a
barra: abandone a ignorância. Escave com a faca, você, sábio.’ Esse é o
significado.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O sapo
simboliza o desespero devido à raiva. ‘Jogue fora o sapo: abandone o desespero
devido à raiva. Escave com a faca, você, sábio.’Esse é o significado.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O garfo
simboliza a dúvida. [<a href="#N6">6</a>] ‘<a name=R6></a>Jogue fora o garfo:
abandone a dúvida. Escave com a faca, você, sábio.’ Esse é o significado.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“A peneira
simboliza os cinco obstáculos, isto é, o obstáculo do desejo sensual, o
obstáculo da má vontade, o obstáculo do torpor e preguiça, o obstáculo da
inquietação e ansiedade, o obstáculo da dúvida. ‘Jogue fora a peneira: abandone
os obstáculos. Escave com a faca, você, sábio.’ Esse é o significado.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O jabuti
simboliza os cinco agregados influenciados pelo apego, [<a href="#N7">7</a>] <a
name=R7></a>isto é, o agregado da forma material influenciado pelo apego, o
agregado da sensação influenciado pelo apego, o agregado da percepção
influenciado pelo apego, o agregado das formações volitivas influenciado pelo apego e o
agregado da consciência influenciado pelo apego. ‘Jogue fora o jabuti: abandone
os cinco agregados influenciados pelo apego. Escave com a faca, você, sábio.’
Esse é o significado.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O machado
e o cepo simbolizam os cinco elementos do prazer sensual [<a href="#N8">8</a>]
- <a name=R8></a>f</span>ormas percebidas através do olhoo que são desejáveis,
agradáveis e fáceis de serem gostadas, conectadas com o desejo sensual e que
provocam a cobiça. Sons percebidos através do ouvido … Aromas percebidos
através do nariz … Sabores percebidos através da língua …Tangíveis percebidos
através do corpo que são desejáveis, agradáveis e fáceis de serem gostados,
conectados com o desejo sensual e que provocam a cobiça<span lang=PT-BR
style='mso-ansi-language:PT-BR'>. ‘Jogue fora o machado e o cepo: abandone os
cinco elementos do prazer sensual. Escave com a faca, você, sábio.’ Esse é o
significado.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O pedaço
de carne simboliza o deleite e a cobiça.[<a href="#N9">9</a>] ‘<a name=R9></a>Jogue
fora o pedaço de carne: abandone o pedaço de carne. Escave com a faca, você,
sábio.’ Esse é o significado.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“A serpente
Naga simboliza um bhikkhu que destruiu as impurezas. [<a href="#N10">10</a>] <a
name=R10></a>‘Deixe a serpente Naga; não cause dano à serpente Naga; honre a
serpente Naga.’ Esse é o significado.”<o:p></o:p></span></p>


<p class=Normal>Isso foi o que disse o Abençoado. O<span
style="mso-spacerun: yes">  </span><span lang=PT-BR style='mso-ansi-language:
PT-BR'>venerável Kumara Kassapa</span> ficou satisfeito e contente com as
palavras do Abençoado<span lang=PT-BR style='mso-ansi-language:PT-BR'>.<o:p></o:p></span></p>


