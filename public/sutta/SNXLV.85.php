através do desenvolvimento de uma maneira tão efetiva quanto por isto: perfeição
da virtude ... perfeição do desejo ... perfeição do eu ... perfeição do
entendimento ... perfeição da diligência ... perfeição da atenção com
sabedoria [<a href="#N1">1</a>] <a name=R1></a>Quando um bhikkhu tem a atenção
com sabedoria perfeita, é de se esperar que ele desenvolva e se dedique ao
nobre caminho óctuplo. <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como um
bhikkhu que tem a atenção com sabedoria perfeita desenvolve e cultiva o Nobre
Caminho Óctuplo? Aqui, bhikkhus, um bhikkhu desenvolve o entendimento correto,
que tem como objetivo último a remoção da cobiça, a remoção da raiva, a remoção
da delusão ... Ele desenvolve a concentração correta, que tem como objetivo
último a remoção da cobiça, a remoção da raiva, a remoção da delusão. É dessa
forma, bhikkhus, que um bhikkhu que tem a atenção com sabedoria perfeita
desenvolve e cultiva o Nobre Caminho Óctuplo.”<o:p></o:p></span></p>


