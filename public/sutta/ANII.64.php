<p class=Normal> (64) "Bhikkhus, há esses dois tipos de felicidade. Quais dois? A felicidade da pessoa leiga e a felicidade daquele que seguiu a vida santa. Esses são os dois tipos de felicidade. Desses dois tipos de felicidade, a felicidade daquele que seguiu a vida santa é superior." </p>

<p class=Normal> (65) "Bhikkhus, há esses dois tipos de felicidade. Quais dois? A felicidade dos prazeres dos sentidos e a felicidade da renúncia. Esses são os dois tipos de felicidade. Desses dois tipos de felicidade, a felicidade da renúncia é superior." </p>

<p class=Normal> (66) "Bhikkhus, há esses dois tipos de felicidade. Quais dois? A felicidade que depende das aquisições e a felicidade sem aquisições. Esses são os dois tipos de felicidade. Desses dois tipos de felicidade, a felicidade sem aquisições é superior." </p>

<p class=Normal>Nota: veja aquisições, <a href="../glossario.php#upadhi"><i>upadhi</i></a> em pali, no glossario.</p>

<p class=Normal> (67) "Bhikkhus, há esses dois tipos de felicidade. Quais dois? A felicidade conectada com as impurezas e a felicidade sem impurezas. Esses são os dois tipos de felicidade. Desses dois tipos de felicidade, a felicidade sem impurezas é superior." </p>

<p class=Normal>Nota: veja impurezas, <a href="../glossario.php#asava"><i>asava</i></a> em pali, no glossario.</p>

<p class=Normal> (68) "Bhikkhus, há esses dois tipos de felicidade. Quais dois? A felicidade mundana e a felicidade espiritual. Esses são os dois tipos de felicidade. Desses dois tipos de felicidade, a felicidade espiritual é superior." </p>

<p class=Normal> (69) "Bhikkhus, há esses dois tipos de felicidade. Quais dois? A felicidade nobre e a felicidade ignóbil Esses são os dois tipos de felicidade. Desses dois tipos de felicidade, a felicidade nobre é superior." </p>

<p class=Normal> (70) "Bhikkhus, há esses dois tipos de felicidade. Quais dois? A felicidade corporal e a felicidade mental. Esses são os dois tipos de felicidade. Desses dois tipos de felicidade, a felicidade mental é superior." </p>

<p class=Normal> (71) "Bhikkhus, há esses dois tipos de felicidade. Quais dois? A felicidade acompanhada de êxtase e a felicidade sem êxtase. Esses são os dois tipos de felicidade. Desses dois tipos de felicidade, a felicidade sem êxtase é superior." </p>

<p class=Normal>Nota: A felicidade com êxtase (<i>piti</i>) é o segundo jhana e sem êxtase o terceiro jhana. </p>

<p class=Normal> (72) "Bhikkhus, há esses dois tipos de felicidade. Quais dois? A felicidade prazerosa e a felicidade da equanimidade. Esses são os dois tipos de felicidade. Desses dois tipos de felicidade, a felicidade da equanimidade é superior." </p>

<p class=Normal>Nota: a felicidade prazerosa equivale aos três primeiros jhanas. A felicidade da equanimidade é o quarto jhana. </p>

<p class=Normal> (73) "Bhikkhus, há esses dois tipos de felicidade. Quais dois? A felicidade da concentração e a felicidade sem concentração. Esses são os dois tipos de felicidade. Desses dois tipos de felicidade, a felicidade da concentração é superior." </p>

<p class=Normal> (74) "Bhikkhus, há esses dois tipos de felicidade. Quais dois? A felicidade baseada na presença de êxtase e a felicidade baseada na ausência de êxtase. Esses são os dois tipos de felicidade. Desses dois tipos de felicidade, a felicidade baseada na ausência de êxtase é superior." </p>

<p class=Normal> (75) "Bhikkhus, há esses dois tipos de felicidade. Quais dois? A felicidade baseada no prazer e a felicidade baseada na equanimidade. Esses são os dois tipos de felicidade. Desses dois tipos de felicidade, a felicidade baseada na equanimidade é superior." </p>

<p class=Normal> (76) "Bhikkhus, há esses dois tipos de felicidade. Quais dois? A felicidade baseada na forma e a felicidade baseada no sem forma. Esses são os dois tipos de felicidade. Desses dois tipos de felicidade, a felicidade baseada no sem forma é superior." </p>

<p class=Normal>Nota: baseada na forma é o quarto jhana. No sem forma são as realizações imateriais. </p>

