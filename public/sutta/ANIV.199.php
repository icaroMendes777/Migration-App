<p class=Normal> “Bhikkhus, eu lhes ensinarei sobre o desejo: esse sedutor que segue fluindo, estendendo-se e aprisionando, através do qual este mundo é enrolado e sufocado como um novelo embaraçado, como uma corda embolada cheia de nós, como juncos enredados, e não consegue escapar da transmigração, dos planos de miséria, dos destinos ruins, dos mundos inferiores. Ouçam e prestem muita atenção àquilo que eu vou dizer.” – “Sim, venerável senhor,” os bhikkhus responderam. O Abençoado disse o seguinte: </p>

<p class=Normal> “E qual desejo é esse sedutor que segue fluindo, estendendo-se e aprisionando, através do qual este mundo é enrolado e sufocado como um novelo embaraçado, como uma corda embolada cheia de nós, como juncos enredados, e não consegue escapar da transmigração, dos planos de miséria, dos destinos ruins, dos mundos inferiores? Essas 18 verbalizações do desejo na dependência do que é interno e as 18 verbalizações do desejo na dependência do que é externo. [<a href="#N1">1</a>] <a name="R1"></a></p> 

<p class=Normal> “E quais são as 18 verbalizações do desejo na dependência do que é interno? Havendo ‘eu sou,’ surge ‘eu estou aqui,’ surge ‘eu sou assim’ ... ‘eu sou diferente’ ... ‘eu sou ruim’ ... ‘eu sou bom’ ... ‘eu poderei ser’ ... ‘eu poderei estar aqui’ … ‘eu poderei ser assim’ … ‘eu poderei ser diferente’ ... ‘que eu seja’ ... ‘que eu esteja aqui’ ... ‘que eu seja assim’ ... ‘que eu seja diferente’ ... ‘eu estarei aqui’ ... ‘eu serei assim’ ... ‘eu serei diferente.’ Essas são as 18 verbalizações do desejo na dependência do que é interno. </p>

<p class=Normal> “E quais são as 18 verbalizações do desejo na dependência do que é externo? Havendo ‘eu sou devido a isso (ou através disso),’ surge ‘eu estou aqui devido a isso,’ surge ‘eu sou assim devido a isso’ ... ‘eu sou diferente devido a isso’ ... ‘eu sou ruim devido a isso’ ... ‘eu sou bom devido a isso’ ... ‘eu poderei ser devido a isso’ ... ‘eu poderei estar aqui devido a isso’ … ‘eu poderei ser assim devido a isso’ … ‘eu poderei ser diferente devido a isso’ ... ‘que eu seja devido a isso’ ... ‘que eu esteja aqui devido a isso’ ... ‘que eu seja assim devido a isso’ ... ‘que eu seja diferente devido a isso’ ... ‘eu estarei aqui devido a isso’ ... ‘eu serei assim devido a isso’ ... ‘eu serei diferente devido a isso.’ Essas são as 18 verbalizações do desejo na dependência do que é externo. </p> 

<p class=Normal> “Portanto há 18 verbalizações do desejo na dependência do que é interno e 18 verbalizações do desejo na dependência do que é externo. Essas são chamadas as 36 verbalizações do desejo. Portanto com 36 verbalizações desse tipo no passado, 36 no futuro e 36 no presente, há 108 verbalizações do desejo. </p> 

<p class=Normal> “Isso, bhikkhus, é o desejo, esse sedutor que segue fluindo, estendendo-se e aprisionando, através do qual este mundo é enrolado e sufocado como um novelo embaraçado, como uma corda embolada cheia de nós, como juncos enredados, e não consegue escapar da transmigração, dos planos de miséria, dos destinos ruins, dos mundos inferiores.” </p>

