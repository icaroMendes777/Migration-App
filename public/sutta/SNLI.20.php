class=GramE>externamente ...</span>. (segue igual acima) ...<o:p></o:p></span></p>



<p class=Normal><span class=GramE><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“É dessa forma, bhikkhus, que um bhikkhu com uma mente aberta e não
confinada, desenvolve a mente imbuída de luminosidade.</span></span><span
lang=PT-BR style='mso-ansi-language:PT-BR'><o:p></o:p></span></p>



<p class=Normal><span class=GramE><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Quando, bhikkhus, as quatro bases do poder espiritual tiverem sido
desenvolvidas e cultivadas desse modo, elas trazem grandes frutos e benefícios.</span></span><span
lang=PT-BR style='mso-ansi-language:PT-BR'><o:p></o:p></span></p>



<p class=Normal><span class=GramE><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Quando</span></span><span lang=PT-BR style='mso-ansi-language:PT-BR'>,
bhikkhus, as quatro bases do poder espiritual tiverem sido desenvolvidas e
cultivadas desse modo, um bhikkhu exerce os vários tipos de poderes
supra-humanos: tendo sido um, ele se torna vários; tendo sido vários, ele se
torna um; ele aparece e desaparece; ele cruza sem nenhum problema uma parede,
um cercado, uma montanha ou através do espaço; ele mergulha e sai da terra como
se fosse água; ele caminha sobre a água sem afundar como se fosse terra;
sentado de pernas cruzadas ele cruza o espaço como se fosse um pássaro; com a
sua mão ele toca e acaricia a lua e o sol tão forte e poderoso; ele exerce
poderes corporais até mesmo nos distantes mundos de Brahma <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando as
quatro bases do <span class=GramE>poder</span> espiritual tiverem sido
desenvolvidas e cultivadas desse modo, um bhikkhu, com o elemento do ouvido
divino, que é purificado e ultrapassa o humano, ouve tanto os sons divinos como
os humanos e tanto aqueles distantes como os próximos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando as
quatro bases do <span class=GramE>poder</span> espiritual tiverem sido
desenvolvidas e cultivadas desse modo, um bhikkhu compreende as mentes de
outros seres, de outras pessoas, abarcando-as com a sua própria mente. Ele
compreende uma mente afetada pelo desejo como afetada pelo desejo e uma mente
não afetada pelo desejo como não afetada pelo desejo; Ele compreende uma mente
afetada pela raiva como afetada pela raiva e uma mente não afetada pela raiva
como não afetada pela raiva; Ele compreende uma mente afetada pela delusão como
afetada pela delusão e uma mente não afetada pela delusão como não afetada pela
delusão; Ele compreende uma mente contraída como contraída e uma mente
distraída como distraída; Ele compreende uma mente transcendente como
transcendente e uma mente não transcendente como não transcendente; Ele
compreende uma mente superável como superável e uma mente não superável como
não superável; Ele compreende uma mente concentrada como concentrada e uma
mente não concentrada como não concentrada; Ele compreende uma mente libertada
como libertada e uma mente não libertada como não libertada.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando as
quatro bases do <span class=GramE>poder</span> espiritual tiverem sido
desenvolvidas e cultivadas desse modo, um bhikkhu se recorda das suas muitas
vidas passadas, isto é, um nascimento, dois nascimentos, três nascimentos,
quatro, cinco, dez, vinte, trinta, quarenta, cinqüenta, cem, mil, cem mil,
muitos ciclos cósmicos de contração, muitas ciclos cósmicos de expansão, muitas
ciclos cósmicos de contração e expansão, ‘Lá eu tinha tal nome, pertencia a tal
clã, tinha tal aparência. Assim era o meu alimento, assim era a minha
experiência de prazer e dor, assim foi o fim da minha vida. Falecendo daquele
estado, eu ressurgi ali. Ali eu também tinha tal nome, pertencia a tal clã,
tinha tal aparência. Assim era o meu alimento, assim era a minha experiência de
prazer e dor, assim foi o fim da minha vida. Falecendo daquele estado, eu
ressurgi aqui.’ Assim ele se recorda das suas muitas vidas passadas nos seus
modos e detalhes.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando as
quatro bases do <span class=GramE>poder</span> espiritual tiverem sido
desenvolvidas e cultivadas desse modo, um bhikkhu, por meio do olho divino, que
é purificado e ultrapassa o humano, vê seres falecendo e renascendo, inferiores
e superiores, bonitos e feios, afortunados e desafortunados. Ele compreende
como os seres prosseguem de acordo com as suas ações desta forma: ‘Esses seres
– dotados de má conduta com o corpo, linguagem e mente, que insultam os nobres,
com o entendimento incorreto e realizando ações sob a influência do
entendimento incorreto – com a dissolução do corpo, após a morte, renasceram no
plano de privação, num destino ruim, nos planos inferiores, no inferno. Porém,
estes seres - dotados de boa conduta com o corpo, linguagem e mente, que não
insultam os nobres, com o entendimento correto e realizando ações sob a
influência do entendimento correto – com a dissolução do corpo, após a morte,
renasceram num bom destino, no paraíso.’ Dessa forma - por meio do olho divino,
que é purificado e ultrapassa o humano, ele vê seres falecendo e renascendo,
inferiores e superiores, bonitos e feios, e ele compreende como os seres
continuam de acordo com as suas ações.. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando as
quatro bases do <span class=GramE>poder</span> espiritual tiverem sido
desenvolvidas e cultivadas desse modo, um bhikkhu, com a eliminação das
impurezas mentais, permanece em um estado livre de impurezas com a libertação
da mente e a libertação pela sabedoria, tendo conhecido e manifestado isso para
si mesmo no aqui e agora.”<o:p></o:p></span></p>

