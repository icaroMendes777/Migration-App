style='mso-ansi-language:PT-BR'> Neste caso, bhikkhus, a percepção da luz é bem
compreendida pelo bhikkhu; a percepção do dia é bem determinada. [<a href="#N2">2</a>]
<a name=R2></a>É dessa forma, bhikkhus, que um bhikkhu com uma mente aberta e
não confinada, desenvolve a mente imbuída de luminosidade.<o:p></o:p></span></p>



