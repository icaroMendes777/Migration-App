<p class=Normal><span class=GramE><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“E o que, bhikkhus, é a investigação demasiado frouxa?</span></span><span
lang=PT-BR style='mso-ansi-language:PT-BR'> É a investigação acompanhada pela
lassidão, associada à lassidão. A isso chamamos de investigação demasiado
frouxa.<o:p></o:p></span></p>



<p class=Normal><span class=GramE><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“E o que, bhikkhus, é a investigação demasiado tensa?</span></span><span
lang=PT-BR style='mso-ansi-language:PT-BR'> É a investigação acompanhada pela
