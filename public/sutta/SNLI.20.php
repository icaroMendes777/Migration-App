penetrada através da sabedoria. É dessa forma, bhikkhus, que um bhikkhu
permanece percebendo depois e antes: ‘Como antes, assim depois; como depois,
assim antes’ [<a href="#N1">1</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“E como, bhikkhus, um bhikkhu permanece percebendo ‘como abaixo, assim
acima; como acima, assim abaixo’? Neste caso, bhikkhus, um bhikkhu examina esse
mesmo corpo para cima <span class=GramE>à</span> partir da sola dos pés e para
baixo a partir do topo da cabeça, limitado pela pele e repleto de muitos tipos
