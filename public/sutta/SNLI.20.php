bhikkhu desenvolve a base do poder espiritual que possui concentração devido ao
desejo e às formações volitivas do esforço através das mesmas qualidades, das
mesmas características, dos mesmos aspectos que ele usa para desenvolver aquela
base do poder espiritual durante o dia. Ou então, durante o dia ele desenvolve a
base do poder espiritual que possui concentração devido ao desejo e às
formações volitivas do esforço através das mesmas qualidades, das mesmas
características, dos mesmos aspectos que ele usa para desenvolver aquela base
do poder espiritual durante a noite. É dessa forma, bhikkhus, que um bhikkhu
permanece ‘como o dia, assim a noite; como a noite, assim o dia.’<o:p></o:p></span></p>



<p class=Normal><span class=GramE><span lang=PT-BR style='mso-ansi-language:
