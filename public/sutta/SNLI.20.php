<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
bhikkhus, um bhikkhu desenvolve a base do poder espiritual que possui
concentração devido ao desejo e às formações volitivas do esforço, pensando:
‘Assim, o meu desejo não estará nem demasiado frouxo e nem demasiado tenso; e
