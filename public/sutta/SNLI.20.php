pele, carne, tendões, ossos, tutano, rins, coração, fígado, diafragma, baço,
pulmões, intestino grosso, intestino delgado, conteúdo do estômago, fezes,
bílis, fleuma, pus, sangue, suor, gordura, lágrimas, saliva, muco, líquido
sinovial, e urina.’ É dessa forma, bhikkhus, que um bhikkhu permanece percebendo
‘como abaixo, assim acima;como acima, assim abaixo.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como,
bhikkhus, um bhikkhu permanece ‘como o dia, assim <span class=GramE>a</span>
