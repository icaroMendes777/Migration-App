<p class=Normal> “Bhikkhus, para aquele que desfruta dos prazeres dos sentidos, a pobreza é sofrimento no mundo.” </p>

<p class=Normal> “Assim é, venerável senhor.” </p>

<p class=Normal> “E uma pessoa pobre, destituída, sem dinheiro, se endivida. Para aquele que desfruta dos prazeres dos sentidos, endividar-se é sofrimento no mundo.” </p>

<p class=Normal> “Assim é, venerável senhor.” </p>

<p class=Normal> “E uma pessoa pobre, destituída, sem dinheiro, tendo se endividado, deve o pagamentos de juros. Para aquele que desfruta dos prazeres dos sentidos, o pagamento de juros é sofrimento no mundo.” </p>

<p class=Normal> “Assim é, venerável senhor.” </p>

<p class=Normal> “E quando uma pessoa pobre, destituída, sem dinheiro, devendo o pagamento de juros, não paga os juros a tempo, os cobradores a assediam. Para aquele que desfruta dos prazeres dos sentidos, ser assediado é sofrimento no mundo.” </p>

<p class=Normal> “Assim é, venerável senhor.” </p>

<p class=Normal> “E quando uma pessoa pobre, destituída, sem dinheiro, sendo assediada não paga, os cobradores a acossam. Para aquele que desfruta dos prazeres dos sentidos, ser acossado é sofrimento no mundo.” </p>

<p class=Normal> “Assim é, venerável senhor.” </p>

<p class=Normal> “E quando uma pessoa pobre, destituída, sem dinheiro, sendo acossada não paga, os cobradores a aprisionam. Para aquele que desfruta dos prazeres dos sentidos, ser aprisionado é sofrimento no mundo.” </p>

<p class=Normal> “Assim é, venerável senhor.” </p>

<p class=Normal> “Portanto, bhikkhus,  para aquele que desfruta dos prazeres dos sentidos, a pobreza é sofrimento no mundo. Para aquele que desfruta dos prazeres dos sentidos, endividar-se é sofrimento no mundo. Para aquele que desfruta dos prazeres dos sentidos, o pagamento de juros é sofrimento no mundo. Para aquele que desfruta dos prazeres dos sentidos, ser assediado é sofrimento no mundo. Para aquele que desfruta dos prazeres dos sentidos, ser acossado é sofrimento no mundo. Para aquele que desfruta dos prazeres dos sentidos, ser aprisionado é sofrimento no mundo. </p>

<p class=Normal> “Do mesmo modo, bhikkhus, aquele que na disciplina dos nobres, não tem convicção com relação às qualidades mentais hábeis, não tem o senso de vergonha com relação às qualidades mentais hábeis, não tem o senso de temor com relação às qualidades mentais hábeis, não tem energia com relação às qualidades mentais hábeis, não tem sabedoria com relação às qualidades mentais hábeis, é dito ser pobre, destituído. </p>

<p class=Normal> “Ele, pobre, destituído, sem ter a convicção com relação às qualidades mentais hábeis, sem ter o senso de vergonha … sem ter o senso de temor ... sem ter energia ... sem ter sabedoria com relação às qualidades mentais hábeis, pratica ações impróprias com o corpo, ações impróprias com a linguagem, ações impróprias com a mente. Para ele, eu lhes digo, isso é endividar-se. </p>

<p class=Normal> “Tendo como propósito ocultar a sua conduta corporal imprópria, ele formula desejos ruins: Ele deseja, ‘Que eles não saibam o que fiz.’ Ele decide, ‘Que eles não saibam o que fiz.’ Ele fala, [pensando,] ‘Que eles não saibam o que fiz.’ Ele faz esforço com o corpo, [pensando,] ‘Que eles não saibam o que fiz.’ Tendo como propósito ocultar a sua conduta verbal imprópria ... Tendo como propósito ocultar a sua conduta mental imprópria, ele formula desejos ruins: Ele deseja, ‘Que eles não saibam o que fiz.’ Ele decide, ‘Que eles não saibam o que fiz.’ Ele fala, [pensando,] ‘Que eles não saibam o que fiz.’ Ele faz esforço com o corpo, [pensando,] ‘Que eles não saibam o que fiz.’ Para ele, eu lhes digo, isso é o pagamento de juros [para pagar a sua dívida moral]. </p>

<p class=Normal> “Depois, os seus companheiros virtuosos na vida santa dizem a respeito dele, ‘Esse venerável age desse modo, se comporta desse modo.’ Para ele, eu lhes digo, isso é ser assediado. </p>

<p class=Normal> “Depois, tendo ido para a floresta, para a sombra de uma árvore ou para uma cabana vazia, ele está tomado por pensamentos ruins e prejudiciais acompanhados pela ansiedade. Para ele, eu lhes digo, isso é ser acossado. </p>

<p class=Normal> “Ele, [com a virtude] pobre, destituído, tendo se dedicado à conduta imprópria através do corpo, conduta imprópria através da linguagem, conduta imprópria através da mente, com a dissolução do corpo, após a morte, renasce num estado de privação, num destino infeliz, nos reinos inferiores, até mesmo no inferno. E eu não posso imaginar nenhum grilhão tão doloroso, tão obstrutivo para a insuperável libertação contra o cativeiro, como o grilhão do inferno ou o grilhão do ventre animal.” </p>

