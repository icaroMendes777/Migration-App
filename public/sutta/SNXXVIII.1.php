vindo à distância e disse: “Amigo Sariputta, as suas faculdades estão serenas,
a sua complexão está pura e brilhante. Em qual permanência o Venerável
Sariputta passou o dia?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui.
