<p class=Normal> <a name=R1></a>"Bhikkhus, essas sete coisas conduzem ao declínio de um discípulo leigo. Quais sete? (1) Ele para de visitar os bhikkhus; (2) ele negligencia ouvir o bom Dhamma; (3) ele não treina no comportamento virtuoso superior; (4) ele é muito suspeitoso em relação aos bhikkhus, quer sejam seniores, recém ordenados ou médios; (5) ele ouve o Dhamma com uma mente que tende para a crítica, buscando encontrar defeitos; (6) ele busca alguém digno de oferendas entre os de fora; [<a href="#N1">1</a>] (7) ele pratica os atos meritórios primeiro para os de fora. Essas sete coisas conduzem ao declínio de um discípulo leigo. </p>


<p class=Normal>"Bhikkhus, essas sete coisas conduzem ao não-declínio de um discípulo leigo. Quais sete? (1) Ele não para de visitar os bhikkhus; (2) ele não negligencia ouvir o bom Dhamma; (3) ele treina no comportamento virtuoso superior; (4) ele tem muita confiança em relação aos bhikkhus, quer sejam seniores, recém ordenados ou médios; (5) ele ouve o Dhamma com uma mente que não tende para a crítica, não buscando encontrar defeitos; (6) ele não busca alguém digno de oferendas entre os de fora; (7) ele pratica os atos meritórios primeiro aqui. Essas sete coisas conduzem ao não-declínio de um discípulo leigo. </p>


<p class=Normal>O discípulo leigo que para <br>
de visitar os bhikkhus que estão desenvolvidos, <br>
que para de ouvir os ensinamentos dos nobres, <br>
e não treina na virtude superior; <br>
cuja suspeita em relação aos bhikkhus<br>
está sempre aumentando; <br>
que ouve o bom Dhamma<br>
com a mente que tende para a crítica; <br>
o discípulo leigo que busca<br>
alguém digno de oferendas entre os de fora<br>
e pratica atos meritórios<br>
primeiro para os de fora: <br>
esses sete princípios bem ensinados<br>
descrevem aquilo que conduz ao declínio. </p>


