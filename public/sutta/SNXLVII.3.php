<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em certa
ocasião, o Abençoado estava<span style="mso-spacerun: yes">  </span>em Savatthi
no Bosque de Jeta, no Parque de Anathapindika. Então um certo bhikkhu foi até o
Abençoado e depois de cumprimentá-lo sentou a um lado e disse:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, seria bom se o Abençoado pudesse me ensinar o Dhamma de maneira
resumida, de modo que, tendo ouvido o Dhamma do Abençoado, eu possa permanecer
só, isolado, diligente, ardente e decidido.” [<a href="#N1">1</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Mas é justamente dessa maneira que alguns homens tolos fazem um pedido
e então, tendo ouvido o Dhamma, pensam que devem seguir-me aonde quer que eu
vá.” </span><span lang=PT-BR style='mso-bidi-font-size:10.0pt;mso-ansi-language:
PT-BR'><o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Possa o
Abençoado ensinar-me o Dhamma de maneira resumida! Possa o Iluminado ensinar-me
o Dhamma de maneira resumida! É bem possível que eu entenda as palavras do
Abençoado. É bem possível que eu me torne um herdeiro das palavras do
Abençoado.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bem então,
bhikkhu, purifique o ponto exato do início dos estados benéficos. E qual é o
início dos estados benéficos? A virtude que é bem purificada e o entendimento
que é correto. [<a href="#N2">2</a>] <a name=R2></a>Então, bhikkhu, quando a sua
virtude estiver bem purificada e o seu entendimento correto, baseado na
virtude, estabelecido na virtude, você deve desenvolver os quatro fundamentos
da atenção plena de três modos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quais
quatro? Neste caso, bhikkhu, permaneça contemplando o corpo como um corpo
internamente, ardente, plenamente consciente e com atenção plena, tendo
colocado de lado a cobiça e o desprazer pelo mundo. Permaneça contemplando o
corpo como um corpo externamente, ardente, plenamente consciente e com atenção
plena, tendo colocado de lado a cobiça e o desprazer pelo mundo. Permaneça
contemplando o corpo como um corpo internamente e externamente, ardente,
plenamente consciente e com atenção plena, tendo colocado de lado a cobiça e o
desprazer pelo mundo.[<a href="#N3">3</a>]<o:p></o:p></span></p>


<p class=Normal><a name=R3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Permaneça contemplando as sensações como sensações internamente ...
externamente ... internamente e externamente, ardente, plenamente consciente e
com atenção plena, tendo colocado de lado a cobiça e o desprazer pelo mundo.
Permaneça contemplando a mente como mente internamente ... externamente ...
internamente e externamente, ardente, plenamente consciente e com atenção
plena, tendo colocado de lado a cobiça e o desprazer pelo mundo. Permaneça
contemplando os objetos mentais como objetos mentais internamente ...
externamente ... internamente e externamente, ardente, plenamente consciente e
com atenção plena, tendo colocado de lado a cobiça e o desprazer pelo mundo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando,
bhikkhu, baseado na virtude, estabelecido na virtude, você desenvolver os
quatro fundamentos da atenção plena dessa forma, então, quer venha a noite ou o
dia, você poderá esperar apenas o incremento nos estados benéficos, não o seu
declínio.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então,
aquele bhikkhu, tendo ficado satisfeito e contente com as palavras do Abençoado,
levantou-se do seu assento e depois de homenageá-lo, mantendo-o à sua direita,
partiu. Permanecendo só, isolado, diligente, ardente e decidido, em pouco
tempo, ele alcançou e permaneceu no objetivo supremo da vida santa pelo qual
membros de um clã deixam a vida em família pela vida santa, tendo conhecido e
realizado por si mesmo no aqui e agora. Ele soube: “O nascimento foi destruído,
a vida santa foi vivida, o que deveria ser feito foi feito, não há mais vir a
ser a nenhum estado.” E assim aquele bhikkhu tornou-se mais um dos Arahants.<o:p></o:p></span></p>


