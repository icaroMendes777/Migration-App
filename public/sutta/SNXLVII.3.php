<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Notas:<o:p></o:p></span></b></p>



<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] O comentário explica que este bhikkhu depois de solicitar que o Buda
explicasse um objeto de meditação ficou só perambulando por aqui e ali sem se dedicar
ao isolamento. Por isso o Buda respondeu do modo que segue. [<a href="#R1">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[2] O entendimento é aquele da responsabilidade pelas próprias ações,
isto é, a crença no kamma e nos seus frutos, o que também implica a crença no
renascimento. Com esse enunciado o Buda estabelece o entendimento correto, (o
primeiro elemento do nobre caminho óctuplo), e a conduta correta, (os elementos
3, 4 e 5 do nobre caminho óctuplo), como base para uma boa prática dos
quatro fundamentos da atenção plena.[<a href="#R2">Retorna</a>] <o:p></o:p></span></p>



<p class=Normal><a name=N3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[3] Com relação ao corpo, a contemplação é feita através da respiração,
contemplando a própria respiração e a respiração dos outros. Ou seja a mente do
meditador se move para o interior e exterior sem deixar de lado o objeto da
meditação que é a respiração. No caso das sensações e mente, o processo seria o
mesmo apesar do comentário não explicar como isso deve ser feito, no caso dos
outros, sem o auxílio de poderes supra-humanos. [<a href="#R3">Retorna</a>]<o:p></o:p></span></p>






