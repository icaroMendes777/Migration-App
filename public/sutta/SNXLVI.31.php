<p class=Normal> “Bhikkhus, todos os estados que existem que são benéficos, que partilham do benéfico, que pertencem ao que é benéfico, todos eles estão enraizados na diligência, convergem para a diligência e a diligência é declarada como a principal dentre eles. Quando um bhikkhu é diligente, é de se esperar que ele desenvolva e cultive os sete fatores da iluminação. </p>
 
<p class=Normal> “E como, bhikkhus, um bhikkhu que é diligente desenvolve e cultiva os sete fatores da iluminação? Nesse caso, bhikkhus, um bhikkhu desenvolve o fator da iluminação da atenção plena, que tem como base o afastamento, desapego e cessação, que amadurece no abandono ... ele desenvolve o fator da iluminação da equanimidade, que tem como base o afastamento, desapego e cessação, que amadurece no abandono. É desse modo, bhikkhus, que um bhikkhu que é diligente desenvolve e cultiva os sete fatores da iluminação.”</p>


