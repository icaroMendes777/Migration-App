<p class=Normal>"Se a contenção das faculdades estiver ausente, bhikkhus, então a base para a virtude é destruída naquele em que a contenção das faculdades está ausente. Se a virtude estiver ausente, então a base para a concentração correta é destruída naquele em que a virtude está ausente. Se a concentração correta estiver ausente, então a base para o conhecimento e visão de como as coisas na verdade são é destruída naquele em que a concentração correta está ausente. Se o conhecimento e visão de como as coisas na verdade são estiver ausente, então a base para o desencantamento e o desapego é destruída naquele em que o conhecimento e visão de como as coisas na verdade são está ausente. Se o desencantamento e o desapego estiverem ausentes, então a base para o conhecimento e visão da libertação é destruída naquele em que o desencantamento e o desapego estão ausentes. [<a href="#N1">1</a>] </p>

<p class=Normal><a name="R1"></a>"É igual a uma árvore sem galhos e folhas: os brotos não irão amadurecer; nem a casca, nem o alburno e nem o cerne irão amadurecer. Assim também, se a contenção dos sentidos estiver ausente, não haverá base para a virtude ... a base para o conhecimento e visão da libertação é destruída naquele em que o desencantamento e o desapego estão ausentes. </p>

<p class=Normal>"Mas se a contenção das faculdades dos sentidos estiver presente, bhikkhus, então a virtude terá uma base naquele em que a contenção das faculdades está presente. Se a virtude estiver presente, a concentração correta terá uma base naquele em que a virtude está presente. Se a concentração correta estiver presente, o conhecimento e visão de como as coisas na verdade são terá uma base naquele em que a concentração correta está presente. Se o conhecimento e visão de como as coisas na verdade são estiver presente, o desencantamento e o desapego terão uma base naquele em que o conhecimento e visão de como as coisas na verdade são está presente. Se o desencantamento e o desapego estiverem presentes, o conhecimento e visão da libertação terá uma base naquele em que o desencantamento e o desapego estão presentes. </p>

<p class=Normal>"É igual a uma árvore com galhos e folhas: os brotos irão amadurecer; a casca, o alburno e o cerne irão amadurecer. Assim também, se a contenção das faculdades dos sentidos estiver presente,  então a virtude terá uma base ... o conhecimento e visão da libertação terá uma base naquele em que o desencantamento e o desapego estão presentes." </p>



