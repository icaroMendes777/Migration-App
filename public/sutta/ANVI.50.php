<p class=Normal><a name="N1"></a> [1] AA explica “conhecimento e visão das coisas como elas na verdade são”, (<i>yathabhuta-ñanadassana</i>), como insight tenro; desencantamento, (<i>nibida</i>), como insight poderoso; desapego, (<i>viraga</i>), como o nobre caminho supramundano, (os quatro estágios da iluminação); “conhecimento e visão da libertação”, (<i>vimutti-ñanadassana</i>) como o conhecimento do exame. <i>Vimutti</i> equivale ao fruto do arahant, (<i>arahatta-phala</i>).<br> A concentração correta equivale aos jhanas, portanto esta sequência implica que sem os jhanas não há o “conhecimento e visão de como as coisas na verdade são.” [<a href="#R1">Retorna</a>] </p>
 
