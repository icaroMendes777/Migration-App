<p class=Normal>Então, o errante Moliyasivaka foi até o Abençoado e ambos se cumprimentaram. Quando a conversa cortês e amigável havia terminado, ele sentou a um lado e disse para o Abençoado: </p>

<p class=Normal> “’O Dhamma é visível no aqui e agora,’ assim dizem. De que modo o Dhamma é visível no aqui e agora, com efeito imediato, que convida ao exame, que conduz para adiante, para ser experimentado pelos sábios por eles mesmos?” </p>

<p class=Normal> “Muito bem, Sivaka, eu lhe farei uma contra pergunta. Responda como quiser. O que você pensa: quando a cobiça está presente em você, você discerne que ‘a cobiça está presente em mim’? E quando a cobiça não está presente em você, você discerne que ‘a cobiça não está presente em mim’?” </p>

<p class=Normal> “Sim, venerável senhor.” </p>

<p class=Normal> “O fato de, quando a cobiça está presente em você, você discernir que ela está presente; e quando a cobiça não está presente em você, você discernir que ela não está presente: esse é um dos modos através do qual o Dhamma é visível no aqui e agora. </p> 

<p class=Normal> “O que você pensa: quando a raiva está presente em você ... quando a delusão está presente em você ... quando a raiva não está presente em você ... quando a delusão não está presente em você, você discerne que ‘a delusão não está presente em mim’?” </p>

<p class=Normal> “Sim, venerável senhor.” </p>

<p class=Normal> “O fato de, quando a delusão não está presente em você, você discernir que ‘a delusão não está presente em mim’: esse é um dos modos através do qual o Dhamma é visível no aqui e agora.” </p>

<p class=Normal> “Desse modo, Sivaka, o Dhamma é visível no aqui e agora, com efeito imediato, que convida ao exame, que conduz para adiante, para ser experimentado pelos sábios por eles mesmos.” </p>

