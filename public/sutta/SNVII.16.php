<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. Agora, naquela ocasião, um brâmane chamado Paccanikasata,
Gozador da Contradição, estava em Savatthi. Então, ocorreu ao brâmane Paccanikasata:
“Vou até o contemplativo Gotama e contradirei tudo que ele disser.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Agora,
naquela ocasião o Abençoado estava caminhando para cá e para lá ao ar livre.
Então, o brâmane Paccanikasata foi até o Abençoado e enquanto ele caminhava disse:
“Fale o Dhamma, contemplativo!”<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[O Abençoado:]<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Um conselho cortês é difícil de entender <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>por alguém que goza com as contradições,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>por alguém com a mente corrompida<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>que está fascinado com a agressão.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Mas se ele removeu a agressão<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>e a desconfiança do seu coração,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>se ele deixou de lado a aversão,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>será capaz de compreender um conselho cortês.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isso
foi dito, o brâmane Paccanikasata disse para o Abençoado:<span
style='mso-spacerun:yes'>  </span>“Magnífico, Mestre Gotama! Magnífico, Mestre
Gotama! Mestre Gotama esclareceu o Dhamma de várias formas, como se tivesse
colocado em pé o que estava de cabeça para baixo, revelasse o que estava
escondido, mostrasse o caminho para alguém que estivesse perdido ou segurasse
uma lâmpada no escuro para aqueles que possuem visão pudessem ver as formas. Nós
buscamos refúgio no Mestre Gotama, no Dhamma e na Sangha dos bhikkhus. Que o
Mestre Gotama me aceite como o discípulo leigo que buscou refúgio
para o resto da vida.”<o:p></o:p></span></p>

