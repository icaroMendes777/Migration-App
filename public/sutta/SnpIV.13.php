<p class=Normal style='margin-left:.75in'> “Aqueles que, apegados às suas idéias, <br>

disputam, dizendo, ‘Apenas isso é verdadeiro’:<br>

todos eles são censuráveis,<br>

ou também podem ser elogiados?”<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'> “[O elogio:] É uma coisa tão
insignificante,<br>

que de modo algum tranquiliza.<br>

Eu digo que as disputas produzem dois tipos de resultado;<br>

tendo visto isso, que ninguém dispute, <br>


e realize nibbana<br>


onde não há disputa.<br>




Aquele que é sábio<br>


não adota as idéias <br>


que tenham surgido entre as pessoas no mundo.<br>


Aquele que está livre das idéias<br>


que não tem preferências<br>


em relação ao que é visto, ao que é
ouvido,<br>


porque ele se envolveria?<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'> Aqueles que consideram <br>

que a prática da virtude<br>


é suprema<br>


dizem que a pureza<br>


é alcançada com a auto-contenção.<br>


Adotando uma prática,<br>


eles se dedicam a isso:<br>


‘Vamos apenas treinar nisso,<br>


então haverá purificação.’<br>


Mas esses assim chamados expertos<br>


ainda estão imersos no samsara.<br>


Mas se algum deles falha<br>


nos seus preceitos ou prática,<br>


ele treme,<br>


tendo fracassado nas suas ações.<br>


Ele anseia pela pureza<br>


da mesma forma que um viajante distante de casa <br>


que tenha perdido a sua  caravana.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>  Mas aquele que abandonou<br>


preceitos e práticas [<a
href="#N1">1</a>]<br>


<a name=R1></a>-
tudo -<br>


coisas que são censuráveis, isentas de
censura, [<a href="#N2">2</a>]<br>


<a name=R2></a>sem desejar pelo
‘puro ou impuro,’ [<a href="#N3">3</a>]<br>


<a name=R3></a>viverá com compaixão
e paz,<br>


sem se
ocupar com a paz,<br>


desapegado.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'> Dependendo<br>


de tabus, austeridades,<br>


ou daquilo que é visto, ouvido, ou
sentido,<br>


eles elogiam a pureza em alta voz - <br>




mas eles não estão livres do desejo por novas existências.<br>


Pois aquele que deseja, gera mais desejos;<br>


ele treme, deludido pelas fantasias.<br>


Mas aquele que aqui<br>


superou a morte e renascimento:<br>


porque ele tremeria?<br>


O que desejaria?”<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'> “Aquela idéia que alguns dizem ser ‘suprema,’<br>

é exatamente aquilo que outros dizem ser ‘inferior.’<br>

Qual afirmativa é verdadeira<br>

quando todos eles dizem ser expertos?”</p>



<p class=Normal style='margin-left:.75in'> “Cada um reivindica que a sua própria idéia  é perfeita<br>


enquanto que a dos outros é
inferior.<br>


Assim argumentando, eles disputam,<br>


cada um dizendo que a sua opinião
é a correta.<br>



Se uma idéia é inferior,<br>


devido ao que é dito por um
oponente,<br>


então nenhum ensinamento seria<br>


superlativo,<br>


porque cada um diz<br>


que o ensinamento do outro é inferior<br>


enquanto que apenas o próprio é verdadeiro.<br>


As idéias sendo valorizadas,<br>
assim também são as suas práticas.<br>


Se as idéias deles são verdadeiras<br>
então a pureza deles também lhes é específica.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'> Para o brâmane não há nada<br>


dirigido por outrem,<br>


nada para ser adotado das doutrinas.<br>


Ele portanto superou as disputas,<br>


pois ele não considera <br>


a idéia de alguém outro como a melhor.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>‘Eu sei. Eu vejo. Assim é como é!’ -
<br>


assim dizendo, alguns afirmam a pureza através de uma certa idéia.<br>


Mas qual o propósito em dizer ter ‘visto’ (a verdade),<br>


se idéias rivais são apresentadas.<br>



Uma pessoa vê a mentalidade-materialidade (nome e forma).<br>


Tendo visto, as toma como permanentes. <br>


Não importa se ele viu pouco, muito,<br>


o experto não afirma a pureza<br>


com base nisso.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'> Uma pessoa entrincheirada nas suas
idéias,<br>


sendo enganada pelas idéias,<br>


não é fácil de ser treinada.<br>


Qualquer coisa da qual dependa<br>


ela descreve com adorável,<br>


diz ser a pureza,<br>


que nisso ela viu a
verdade.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>O brâmane, tendo visto com o entendimento,<br>


não se envolve em especulações,<br>


não segue idéias,<br>


não está atado nem mesmo ao
conhecimento.[<a href="#N4">4</a>]<br>


<a name=R4></a>E sabendo <br>


o que é convencional, comum,<br>


ele permanece equânime:<br>


‘Isso é ao que os outros
se apegam.’<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'> Tendo desfeito os nós<br>


que o amarram,<br>


o sábio aqui no mundo<br>


não segue uma facção<br>


quando surge uma disputa.<br>


Em paz entre aqueles que não estão
em paz,<br>


ele permanence equânime, sem se
apegar:<br>


‘Isso é ao que os outros
se apegam.’<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'> Abandonando antigas impurezas,<br>


sem criar novas,<br>


nem se entregando ao desejo,<br>


tampouco entrincheirado nas suas
idéias,<br>


ele está totalmente libertado,<br>


dos pontos de vista,<br>


iluminado.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'> Ele nem adere ao mundo,<br>


tampouco crítica a si mesmo;<br>


está livre de inimigos - [<a href="#N5">5</a>]<br>


<a name=R5></a>com relação a todas
as coisas<br>


vistas, ouvidas, ou sentidas.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'> Com o fardo posto de lado,<br>


o sábio totalmente libertado,<br>


livre
de especulações,<br>


sem desejar nada.” <o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>Assim disse o Abençoado</span>. </p>

