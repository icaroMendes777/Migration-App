<p class=Normal> (141) "Bhikkhus, há dois tipos de presente. Quais dois? O presente de bens materiais e o presente do Dhamma. Esses são os dois tipos de presentes. Desses dois tipos, o presente do Dhamma é superior." </p>

<p class=Normal> (142) "Bhikkhus, há dois tipos de oferenda. Quais dois? A oferenda de bens materiais e a oferenda do Dhamma. Esses são os dois tipos de oferendas. Desses dois tipos, a oferenda do Dhamma é superior." </p>

<p class=Normal> (143) "Bhikkhus, há dois tipos de generosidade. Quais dois? A generosidade com bens materiais e a generosidade com o Dhamma. Esses são os dois tipos de generosidade. Desses dois tipos, a generosidade com o Dhamma é superior." </p>

<p class=Normal> (144) "Bhikkhus, há dois tipos de renúncia. Quais dois? A renúncia aos bens materiais e a renúncia [dando] o Dhamma. Esses são os dois tipos de renúncia. Desses dois tipos, a renúncia [dando] o Dhamma é superior." </p>

<p class=Normal> (145) "Bhikkhus, há dois tipos de riqueza. Quais dois? A riqueza de bens materiais e a riqueza do Dhamma. Esses são os dois tipos de riqueza. Desses dois tipos, a riqueza o Dhamma é superior." </p>

<p class=Normal> (146) "Bhikkhus, há dois tipos de prazer. Quais dois? O prazer de bens materiais e o prazer do Dhamma. Esses são os dois tipos de prazer. Desses dois tipos, o prazer do Dhamma é superior." </p>

<p class=Normal> (147) "Bhikkhus, há dois modos de compartir. Quais dois? Compartir bens materiais e compartir o Dhamma. Esses são os dois modos de compartir. Desses dois modos, compartir o Dhamma é superior." </p>

<p class=Normal> (148) "Bhikkhus, há dois modos de manter um relacionamento positivo. Quais dois? Manter um relacionamento positivo com bens materiais e manter um relacionamento positivo com o Dhamma. Esses são os dois modos de manter um relacionamento positivo. Desses dois modos, manter um relacionamento positivo com o Dhamma é superior." </p>

<p class=Normal> (149) "Bhikkhus, há dois tipos de assistência. Quais dois? Assistência com bens materiais e assistência com o Dhamma. Esses são os tipos de assistência. Desses dois tipos, a assistência com o Dhamma é superior." </p>

<p class=Normal> (150) "Bhikkhus, há dois tipos de compaixão. Quais dois? Compaixão praticada com bens materiais e compaixão praticada com o Dhamma. Esses são os tipos de compaixão. Desses dois tipos, compaixão praticada com o Dhamma é superior." </p>

<p class=Normal>Nota: compaixão é <i>anukampa</i> em pali que em geral significa a compaixão ativa, em contraste com <i>karuna</i> que em geral significa a compaixão na meditação. </p>



