<p class=Tit3 align=center style='text-align:center'><b> Anguttara Nikaya V.28</b></Tit3>
<p class=Tit1 align=center style='text-align:center'>Samadhanga Sutta </Tit1>
<p class=Tit1 align=center style='text-align:center'>Os Fatores da Concentração </Tit1>

<p class=Normal align=center style='text-align:center'><span lang=PT-BR style='font-size:
