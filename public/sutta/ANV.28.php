<p class=Normal style='margin-left:.75in'>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ouvi que em certa ocasião o
Abençoado estava em Savatthi, no Bosque de Jeta, no Parque de Anathapindika. Lá
ele se dirigiu aos monges desta forma: &quot;Bhikkhus, eu ensinarei os cinco
fatores da nobre concentração correta. Ouçam e prestem muita atenção àquilo que
eu vou dizer.” – “Sim, venerável senhor,” eles responderam. O Abençoado
disse o seguinte:<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-spacerun:yes'> </span>&quot;Agora qual, bhikkhus, são os cinco
fatores da nobre concentração correta? <o:p></o:p></span></p>

<p class=Normal><a name=J1> </a>
&quot;É o caso em que um bhikkhu afastado dos prazeres sensuais, afastado das qualidades
não hábeis, entra e permanece no primeiro jhana, que é caracterizado
pelo pensamento aplicado e sustentado, com o êxtase e felicidade nascidos do
afastamento. Ele permeia e impregna, cobre e preenche o corpo com o êxtase e
felicidade nascidos do afastamento. Não há nada em todo o corpo que não
esteja permeado pelo êxtase e felicidade nascidos do afastamento. <o:p></o:p></span></p>


<p class=Normal>&quot;É como se um banhista habilidoso ou seu
aprendiz vertesse pó de banho numa bacia de latão e o misturasse, borrifando
com água de tempos em tempos, de forma que essa bola de pó de banho - saturada,
carregada de umidade, permeada por dentro e por fora - no entanto não pingasse;
assim, o bhikkhu permeia, cobre e preenche o corpo com o êxtase e felicidade
nascidos do afastamento.... <o:p></o:p></span></p>


<p class=Normal><a name=J2> </a>
&quot;E além disso, abandonando o pensamento aplicado e sustentado, um bhikkhu
entra e permanece no segundo jhana, que é caracterizado pela segurança interna e
perfeita unicidade da mente, sem o pensamento aplicado e sustentado, com o êxtase e felicidade
nascidos da concentração. Ele permeia e impregna, cobre e preenche o corpo
com o êxtase e felicidade nascidos da concentração. Não há nada em todo o
corpo que não esteja permeado pelo êxtase e felicidade nascidos da
concentração.<o:p></o:p></span></p>



<p class=Normal>&quot;Como
um lago sendo alimentado por uma fonte de água interna, não tendo um fluxo de
água do leste, oeste, norte, ou sul, nem os céus periodicamente fornecendo
chuvas abundantes, de modo que a fonte de água interna permeia e
impregna, cobre e preenche o lago de água fresca, sem que 
nenhuma parte do lago  não esteja permeada pela água fresca; assim também
o bhikkhu permeia e impregna, cobre e preenche o corpo com o êxtase e
felicidade nascidos da concentração... <o:p></o:p></span></p>



<p class=Normal><a name=J3> </a>
&quot;E além disso, abandonando  o êxtase, um bhikkhu entra e permanece no terceiro jhana que é caracterizado pela felicidade sem o êxtase, acompanhada pela atenção plena, plena consciência e equanimidade, acerca do qual os
nobres declaram: ‘Ele permanece numa estada feliz, equânime e plenamente
atento.’ Ele permeia e impregna, cobre e preenche o
corpo com a felicidade despojada do êxtase, de forma que não exista nada em
todo o corpo que não esteja permeado com a felicidade despojada do êxtase. <o:p></o:p></span></p>


<p class=Normal>&quot;Como
num lago que tenha flores de lótus azuis, brancas ou vermelhas, podem existir
algumas flores de lótus azuis, brancas, ou vermelhas que, nascidas e tendo
crescido na água, permanecem imersas na água e florescem sem sair de dentro da
água, de forma que elas permanecem permeadas e impregnadas, cobertas e
preenchidas com água fresca da raiz até a ponta, e nada dessas flores de lótus
azuis, brancas ou vermelhas permanece sem estar permeado pela água fresca;
assim também o bhikkhu permeia e impregna, cobre e preenche o corpo com a
felicidade despojada de êxtase ... <o:p></o:p></span></p>



<p class=Normal><a name=J4> </a>
&quot;E além disso, com o completo desaparecimento da felicidade, um bhikkhu entra e permanece no
quarto jhana, que possui nem felicidade nem sofrimento, com a atenção plena
e a equanimidade purificadas. Ele permanece permeando o corpo com a
mente pura e luminosa, de forma que não exista nada em todo o corpo
que não esteja permeado pela mente pura e luminosa. <o:p></o:p></span></p>


<p class=Normal>&quot;Como
se um homem estivesse enrolado da cabeça aos pés com um tecido branco
de forma que não houvesse nenhuma parte do corpo que não estivesse coberta pelo
tecido branco; assim também o bhikkhu permanece permeando o corpo com a
mente pura e luminosa. Não há nada
no corpo que não esteja permeado por essa mente pura e luminosa&quot; </span><span lang=PT-BR
style='font-family:Arial;mso-ansi-language:PT-BR'><o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;E além disso, o
bhikkhu tem o seu objeto de meditação sob total controle, se ocupa com ele, o
considera, está bem sintonizado com ele (boa compreensão) por meio da
sabedoria. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Como se uma pessoa
refletisse acerca de outra, ou uma pessoa em pé refletisse acerca de uma pessoa
sentada, ou uma pessoa sentada refletisse sobre uma pessoa deitada, assim
também, bhikkhus, o bhikkhu tem o seu objeto de meditação sob total controle,
se ocupa com ele, o considera, está bem sintonizado com ele (boa compreensão)
por meio da sabedoria. Esse é o quinto desenvolvimento dos cinco fatores da
nobre concentração correta.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Quando o bhikkhu
perseverou e desenvolveu os cinco fatores da nobre concentração correta dessa
forma, então ao inclinar a sua mente para penetrar e compreender qualquer um
dos seis conhecimentos superiores, ele poderá experimentá-los por si mesmo
sempre que houver uma oportunidade. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Suponham que
houvesse, sobre um suporte, um jarro com água até a borda, de tal forma que
corvos pudessem beber nele. Se um homem forte fosse incliná-lo por menos que
fosse, a água derramaria?&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Sim, senhor.&quot;<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Da mesma forma,
quando um bhikkhu perseverou e desenvolveu os cinco fatores da nobre
concentração correta desta forma, então ao inclinar a sua mente para penetrar e
compreender qualquer um dos seis conhecimentos superiores, ele poderá
experimentá-los por si mesmo sempre que houver uma oportunidade.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Suponham que houvesse
um reservatório quadrado num terreno plano, circundado por um dique, cheio de
água até a borda, de tal forma que corvos pudessem beber nele. Se um homem
forte afrouxasse o dique em qualquer lugar que fosse, a água vazaria?&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Sim, senhor.&quot;<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Da mesma forma,
quando um bhikkhu perseverou e desenvolveu os cinco fatores da nobre
concentração desta forma, então ao inclinar a sua mente para penetrar e
compreender qualquer um dos seis conhecimentos superiores, ele poderá experimentá-los
por si mesmo sempre que houver uma oportunidade.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Suponha que houvesse
uma carruagem num terreno plano numa encruzilhada, com puros-sangues arreados e
a aguilhada preparada, de forma que um adestrador habilidoso, um cocheiro
domador de cavalos pudesse montar nela e, tomando as rédeas na mão esquerda e a
aguilhada na mão direita, pudesse ir e regressar por qualquer caminho quando
ele quisesse; da mesma forma, quando um bhikkhu perseverou e desenvolveu os
cinco fatores da nobre concentração desta forma, então ao inclinar a sua mente
para penetrar e compreender qualquer um dos seis conhecimentos superiores, ele
poderá experimentá-los por si mesmo sempre que houver uma oportunidade.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Se quiser, ele exerce
os vários tipos de poderes supra-humanos: tendo sido um, ele se torna vários;
tendo sido vários, ele se torna um; ele aparece e desaparece; ele cruza sem
nenhum problema uma parede, um cercado, uma montanha ou através do espaço; ele
mergulha e sai da terra como se fosse água; ele caminha sobre a água sem
afundar como se fosse terra; sentado de pernas cruzadas ele cruza o espaço como
se fosse um pássaro; com a sua mão ele toca e acaricia a lua e o sol tão forte
e poderoso; ele exerce poderes corporais até mesmo nos distantes mundos de
Brahma. Ele poderá experimentá-los por si mesmo sempre que houver uma
oportunidade. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Se quiser, com o
elemento do ouvido divino, que é purificado e ultrapassa o humano, ele ouve
tanto os sons divinos como os humanos, aqueles distantes bem como os próximos.
Ele poderá experimentá-los por si mesmo sempre que houver uma oportunidade. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Se quiser, ele
compreende as mentes de outros seres, de outras pessoas, abarcando-as com a sua
própria mente. Ele compreende uma mente afetada pelo desejo como afetada pelo
desejo e uma mente não afetada pelo desejo como não afetada pelo desejo; Ele
compreende uma mente afetada pela raiva como afetada pela raiva e uma mente não
afetada pela raiva como não afetada pela raiva; Ele compreende uma mente
afetada pela delusão como afetada pela delusão e uma mente não afetada pela
delusão como não afetada pela delusão; Ele compreende uma mente contraída como
contraída e uma mente distraída como distraída; Ele compreende uma mente
transcendente como transcendente e uma mente não transcendente como não transcendente;
Ele compreende uma mente superável como superável e uma mente não superável
como não superável; Ele compreende uma mente concentrada como concentrada e uma
mente não concentrada como não concentrada; Ele compreende uma mente libertada
como libertada e uma mente não libertada como não libertada. Ele poderá
experimentá-las por si mesmo sempre que houver uma oportunidade. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Se quiser, ele se
recorda das suas muitas vidas passadas, isto é, um nascimento, dois nascimentos,
três nascimentos, quatro, cinco, dez, vinte, trinta, quarenta, cinqüenta, cem,
mil, cem mil, muitas ciclos de contração cósmica, muitas ciclos de expansão
cósmica, muitas ciclos de contração e expansão cósmica, ‘Lá eu tive tal nome,
pertencia a tal clã, tinha tal aparência. Assim era o meu alimento, assim era a
minha experiência de prazer e dor, assim foi o fim da minha vida. Falecendo
desse estado, eu renasci ali. Ali eu também tinha tal nome, pertencia a tal
clã, tinha tal aparência. Assim era o meu alimento, assim era a minha
experiência de prazer e dor, assim foi o fim da minha vida. Falecendo daquele
estado, eu renasci aqui.’ Assim ele se recorda das suas muitas vidas passadas
nos seus modos e detalhes. Ele poderá experimentá-las por si mesmo sempre que
houver uma oportunidade. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Se
quiser, ele por meio do olho divino, que é purificado e ultrapassa o humano, ele
vê seres falecendo e renascendo, inferiores e superiores, bonitos e feios,
afortunados e desafortunados. Ele compreende como os seres prosseguem de acordo
com as suas ações desta forma: ‘Esses seres – dotados de má conduta corporal,
linguagem, e mente,<span style='mso-spacerun:yes'>  </span>que insultam os
nobres,<span style='mso-spacerun:yes'>  </span>com o entendimento incorreto e
realizando ações sob a influência do entendimento incorreto – com a dissolução
do corpo, após a morte, renasceram num estado de privação, num destino infeliz,
nos reinos inferiores, até mesmo no <span style='mso-bidi-font-weight:bold'>inferno</span>.
Porém estes seres - dotados de boa conduta corporal, linguagem e mente,<span
style='mso-spacerun:yes'>  </span>que não insultam os nobres,<span
style='mso-spacerun:yes'>  </span>com o<span style='mso-spacerun:yes'> 
</span>entendimento correto e realizando ações sob a influência do entendimento
correto – com a dissolução do corpo, após a morte, renasceram num destino
feliz, no paraíso.’ Dessa forma - por meio do olho divino, que é purificado e
ultrapassa o humano, ele vê seres falecendo e renascendo, inferiores e superiores,
bonitos e feios, afortunados e desafortunados. Ele compreende como os seres
prosseguem de acordo com as suas ações. Ele poderá experimentar isso por si
mesmo sempre que houver uma oportunidade. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Se quiser, então,
compreendendo por si mesmo com conhecimento direto, ele aqui e agora entra e
permanece na libertação da mente e na libertação através da sabedoria que são
imaculadas com a destruição de todas as impurezas. Ele poderá experimentar isso
por si mesmo sempre que houver uma oportunidade.&quot;<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o que disse o
Abençoado. Os bhikkhus ficaram satisfeitos e contentes com as palavras do
Abençoado. <o:p></o:p></span></p>

