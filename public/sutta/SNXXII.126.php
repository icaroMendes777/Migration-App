<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. Então um certo bhikkhu foi até o Abençoado e depois de cumprimentá-lo
sentou a um lado e disse: “Venerável senhor, dizem, ‘ignorância, ignorância.’ O
que, venerável senhor, é a ignorância e de que modo alguém está imerso na
ignorância?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
bhikkhu, a pessoa comum sem instrução não compreende a forma sujeita ao
surgimento tal qual na verdade ela é: ‘A forma está sujeita ao surgimento.’ Ele
não compreende a forma sujeita à cessação tal qual na verdade ela é: ‘A forma
está sujeita à cessação.’ Ele não compreende a forma sujeita ao surgimento e
cessação tal qual na verdade ela é: ‘A forma está sujeita ao surgimento e
cessação.’ Ele não compreende a sensação ... percepção ... formações volitivas ...
consciência sujeita ao surgimento<span style="mso-spacerun: yes">  </span>...
sujeita à cessação ... sujeita ao surgimento e cessação tal qual na verdade ela
é: ‘A consciência está sujeita ao surgimento e cessação.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Isso é
chamado ignorância, bhikkhu, e desse modo alguém está imerso na ignorância.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isso
foi dito, aquele bhikkhu disse para o Abençoado: “Venerável senhor, dizem,
‘verdadeiro conhecimento, verdadeiro conhecimento. O que então, venerável senhor,
é o verdadeiro conhecimento e de que modo alguém alcança o verdadeiro
conhecimento?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
bhikkhu, o nobre discípulo bem instruído compreende a forma sujeita ao
surgimento tal qual na verdade ela é:<span style="mso-spacerun: yes"> 
</span>‘A forma está sujeita ao surgimento.’ Ele compreende a forma sujeita à
cessação tal qual na verdade ela é: ‘A forma está sujeita à cessação.’ Ele
compreende a forma sujeita ao surgimento e cessação tal qual na verdade ela é:
‘A forma está sujeita ao surgimento e cessação.’ Ele compreende a sensação ...
percepção ... formações volitivas ... consciência sujeita ao surgimento<span
style="mso-spacerun: yes">  </span>... sujeita à cessação ... sujeita ao
surgimento e cessação tal qual na verdade ela é: ‘A consciência está sujeita ao
surgimento e cessação.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Isso é
chamado o verdadeiro conhecimento, bhikkhu, e desse modo alguém alcança o
verdadeiro conhecimento.”<b style='mso-bidi-font-weight:normal'><o:p></o:p></b></span></p>


