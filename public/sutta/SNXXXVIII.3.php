<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Certa
ocasião, o Venerável Sariputta estava em Magadha, em Nalakagama. Então, o
errante<span style="mso-spacerun: yes">  </span>Jambukhadaka <a name=R1></a>foi
até o Venerável Sariputta e ambos se cumprimentaram. Quando a conversa cortês e
amigável havia terminado, ele sentou a um lado e disse para o Venerável
Sariputta:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigo
Sariputta, quem são os proponentes do Dhamma no mundo? Quem pratica bem no
mundo? Quem são os bem-aventurados no mundo?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqueles,
amigo, que ensinam o Dhamma para o abandono da cobiça, para o abandono da
raiva, para o abandono da delusão: eles são os proponentes do Dhamma no mundo.
Aqueles que estão praticando para o abandono da cobiça, para o abandono da
raiva, para o abandono da delusão: eles estão praticando bem no mundo. Aqueles
que abandonaram a cobiça, a raiva e a delusão, cortaram-nas pela raiz, fizeram
como com um tronco de palmeira, eliminando-as de tal modo que não estarão mais
sujeitos a um futuro surgimento: eles são os bem-aventurados no mundo” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas,
amigo, há um caminho, há um meio para o abandono dessa cobiça, para o abandono
dessa raiva, para o abandono dessa delusão?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Há um
caminho, amigo, há um meio para o abandono dessa cobiça, para o abandono dessa
raiva, para o abandono dessa delusão.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
amigo, é esse caminho, qual é esse meio para o abandono dessa cobiça, para o
abandono dessa raiva, para o abandono dessa delusão?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigo, é o
Nobre Caminho Óctuplo; isto é, entendimento correto, pensamento correto,
linguagem correta, ação correta, modo de vida correto, esforço correto, atenção
plena correta, concentração correta. Esse é o caminho, amigo, esse é o meio
para o abandono dessa cobiça, para o abandono dessa raiva, para o abandono
dessa delusão.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Excelente
é o caminho, amigo, excelente é o meio para o abandono dessa cobiça, para o
abandono dessa raiva, para o abandono dessa delusão. E isso é o suficiente,
amigo Sariputta, para a diligência.”<o:p></o:p></span></p>


