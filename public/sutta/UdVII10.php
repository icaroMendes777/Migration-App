<p class=Normal><a name=R1></a> Assim, ouvi. Certa ocasião, o Abençoado estava em Kosambi, no Monastério de Ghosita. Agora, naquela ocasião, os aposentos das mulheres no palácio real do rei Udena pegou fogo e quinhentas mulheres incluindo Samavari faleceram. [<a href="#N1">1</a>]</p>

<p class=Normal> Então, ao amanhecer, um grande número de bhikkhus se vestiram e tomando a tigela e o manto externo, foram para Kosambi para esmolar alimentos. Depois de haver esmolado em Kosambi e de haver retornado, após a refeição eles foram até o Abençoado e depois de cumprimentá-lo sentaram a um lado e disseram: "Venerável senhor, os aposentos das mulheres no palácio real do rei Udena pegou fogo e quinhentas mulheres incluindo Samavari faleceram. Qual é o destino, qual é o futuro percurso dessas mulheres discípulas leigas?"</p>

<p class=Normal> "Bhikkhus, algumas dessas mulheres discípulas leigas entraram na correnteza, algumas retornarão uma vez, algumas não retornarão. A morte de todas essas mulheres discípulas leigas não foi infrutífera.</p>

<p class=Normal>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p>

<p class=Normal style='margin-left:.75in'> O mundo está aprisionado pela delusão<br>
e apenas aparenta ser capaz. [<a href="#N2">2</a>] <br>
Para um tolo, aprisionado pelo desejo<br>
e envolto pela escuridão,<br>
parece ser eterno,<br>
mas para aquele que vê, não há nada.</p>

