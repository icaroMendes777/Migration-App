<p class=Normal> "Bhikkhus, em qualquer lugar em que bhikkhus se envolvem em rixas e brigas, mergulham em discussões, apunhalam uns aos outros usando as palavras como adagas, não me sinto à vontade nem mesmo em dirigir a atenção para aquilo, sem falar em ir até lá. Eu concluo com relação a eles: 'Com certeza, esses veneráveis abandonaram três coisas e cultivaram três [outras coisas].'</p>

<p class=Normal>"Quais são as três coisas que eles abandonaram? Pensamentos de renúncia, pensamentos de não má vontade e pensamentos de não crueldade. Essas são as três coisas que eles abandonaram. </p>

<p class=Normal>"Quais são as três coisas que eles cultivaram? Pensamentos de desejo sensual, pensamentos de má vontade e pensamentos de crueldade. Essas são as três coisas que eles cultivaram. </p>

<p class=Normal>"Bhikkhus, em qualquer lugar em que bhikkhus se envolvem em rixas e brigas,  ... Eu concluo com relação a eles: 'Com certeza, esses veneráveis abandonaram três coisas e cultivaram três [outras coisas].'</p>

<p class=Normal>"Bhikkhus, em qualquer lugar em que bhikkhus estejam vivendo em concórdia, com respeito mútuo, sem disputas, combinando como leite e água, considerando um ao outro com bondade, eu me sinto à vontade em ir até lá, sem falar em dirigir a atenção para aquilo. Eu concluo com relação a eles: 'Com certeza, esses veneráveis abandonaram três coisas e cultivaram três [outras coisas].'</p>

<p class=Normal>"Quais são as três coisas que eles abandonaram? Pensamentos de desejo sensual, pensamentos de má vontade e pensamentos de crueldade. Essas são as três coisas que eles abandonaram. </p>

