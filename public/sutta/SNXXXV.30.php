<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
eu ensinarei para vocês o caminho que é apropriado para desenraizar todas as concepções.
Ouçam e prestem muita atenção àquilo que eu vou dizer.” – “Sim, venerável
senhor,” os bhikkhus responderam. </span>O Abençoado disse o seguinte:<span
lang=PT-BR style='mso-ansi-language:PT-BR'><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é o caminho apropriado para desenraizar todas as concepções? [<a
href="#N1">1</a>]<a name=R1></a> Aqui, bhikkhus, um bhikkhu não concebe [a si
mesmo como ] o olho, ele não concebe [ a si mesmo] no olho, ele não concebe [a
si mesmo separado] do olho, ele não concebe, ‘O olho é meu.’ [<a href="#N2">2</a>]
<a name=R2></a>Ele não concebe [a si mesmo como ] as formas ... a consciência
no olho ... o contato no olho ... e qualquer sensação que surja tendo o contato
no olho como condição – quer seja prazerosa, dolorosa ou nem prazerosa, nem
dolorosa – ele não concebe [a si mesmo como ] isso, ele não concebe [ a si mesmo]
nisso, ele não concebe [a si mesmo separado] disso, ele não concebe, ‘Isso é
meu.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele não
concebe o ouvido ... Ele não concebe a mente ... os objetos mentais ... a
consciência na mente ... o contato na mente ... e qualquer sensação que surja
tendo o contato na mente como condição – quer seja prazerosa, dolorosa ou nem prazerosa, nem
dolorosa – ele não concebe [a si mesmo como ] isso, ele não concebe [ a si mesmo]
nisso, ele não concebe [a si mesmo separado] disso, ele não concebe, ‘Isso é
meu.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele não
concebe [a si mesmo como ] o todo, ele não concebe [ a si mesmo] no todo, ele não concebe [a si mesmo separado] do todo, ele não
concebe, ‘O todo é meu.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Visto que
ele nada concebe, ele não se apega a nada no mundo. Não se apegando, ele não
fica agitado. Sem estar agitado, ele realiza Nibbana. Ele compreende que: ‘O
nascimento foi destruído, a vida santa foi vivida, o que deveria ser feito foi
feito, não há mais vir a ser a nenhum estado.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Esse,
bhikkhus, é o caminho apropriado para desenraizar todas as concepções.”<o:p></o:p></span></p>




