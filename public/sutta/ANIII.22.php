<p class=Normal> 
“ Existem esses três tipos de pessoas enfermas que podem ser
encontradas no mundo. Quais três?”<o:p></o:p></span></p>


<p class=Normal>
“ Existe o caso da pessoa enferma que – independentemente de que ela receba
ou não<span style="mso-spacerun: yes">  </span>alimentação adequada,
independentemente de que ela receba ou não medicamentos adequados,
independentemente de que ela receba ou não cuidados adequados – não irá se
recuperar dessa enfermidade. Existe o caso da pessoa enferma que – independentemente
de que ela receba ou não<span style="mso-spacerun: yes">  </span>alimentação
adequada, independentemente de que ela receba ou não medicamentos adequados,
independentemente de que ela receba ou não cuidados adequados –<span
style="mso-spacerun: yes">  </span>irá se recuperar dessa enfermidade. Existe o
caso da pessoa enferma que irá se recuperar dessa enfermidade se ela receber
alimentação adequada, medicação adequada e cuidados adequados, porém não irá se
recuperar se não receber.<o:p></o:p></span></p>


<p class=Normal>
“ Agora, é por causa da pessoa enferma que irá se recuperar dessa
enfermidade se ela receber alimentação adequada, medicação adequada e cuidados
adequados - porém não irá se recuperar se não receber – que a alimentação para
os enfermos foi permitida, medicação para os enfermos foi permitida e cuidados
adequados para os enfermos foram permitidos. E porque existe esse tipo de
pessoa enferma, que outros tipos de pessoas enfermas também recebem cuidados
adequados [ contando com a possibilidade de que essas pessoas possam necessitar
e se beneficiar de cuidados adequados].<o:p></o:p></span></p>


<p class=Normal>
“ Existem esses três tipos de pessoas enfermas no mundo.<o:p></o:p></span></p>


<p class=Normal>
“ Da mesma forma, existem esses três tipos de pessoas que podem ser encontradas no mundo que se assemelham aos três
tipos de pessoas enfermas. Quais três ?<o:p></o:p></span></p>


<p class=Normal> Existe o caso da pessoa que - independentemente de ela ver ou não o Tathagata, 
independentemente de ela ouvir ou não o Dhamma e a Disciplina proclamados pelo Tathagata – ela não 
irá se iluminar acerca da validade e da correção de qualidades mentais hábeis.<br> Existe o caso da pessoa 
que – independentemente de ela ver ou não o Tathagata, independentemente de ela ouvir ou não o Dhamma e 
a Disciplina proclamados pelo Tathagata – ela irá se iluminar acerca da validade e da correção de qualidades 
mentais hábeis.<br> Existe o caso da pessoa que irá se iluminar acerca da validade e da correção de qualidades mentais 
hábeis se ela ver o Tathagata e ouvir o Dhamma e a Disciplina proclamados pelo Tathagata, porém não irá se iluminar se não ver e ouvir.<o:p></o:p></span></p>

<p class=Normal>
“ Agora , é por causa da pessoa que irá se iluminar acerca da validade
e correção de qualidades mentais hábeis se ela ver o Tathagata e ouvir o Dhamma
e a Disciplina proclamadas pelo Tathagata - porém não irá se iluminar se<span
style="mso-spacerun: yes">  </span>não ver e ouvir – que o ensinamento do
Dhamma foi permitido. E porque existe esse tipo de pessoas que outros tipos de
pessoas também devem receber os ensinamentos do Dhamma [ contando com a
possibilidade de que essas pessoas possam necessitar e se beneficiar do
ensinamento].<o:p></o:p></span></p>


<p class=Normal>
“ Esses são os três tipos de pessoas que podem ser encontradas no mundo que se assemelham aos três
tipos de pessoas enfermas.<o:p></o:p></span></p>

