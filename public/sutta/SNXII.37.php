<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, este corpo não lhes pertence, nem pertence aos outros.
Vocês devem vê-lo como kamma passado, formado por condições, nascido das
volições, a base para as sensações.” <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Com
relação a isso, bhikkhus, o nobre discípulo bem instruído, considera sabiamente
a origem dependente de todas as coisas, assim, ‘Quando existe isso, aquilo existe;
Com o surgimento disso, aquilo surge. Quando não existe isso, </span>aquilo
também não existe<span lang=PT-BR style='mso-ansi-language:PT-BR'>; com a
cessação disto, aquilo cessa.’ Isto é, da ignorância como condição, as
formações volitivas [surgem]. Das formações volitivas como condição, a consciência. Da consciência
como condição, a mentalidade-materialidade (nome e forma). Da mentalidade-materialidade (nome e forma) como
condição, as seis bases dos sentidos. Das seis bases dos sentidos como
condição, o contato. Do contato como condição, a sensação. Da sensação como
condição, o desejo. Do desejo como condição, o apego. Do apego como condição, o
ser/existir. Do ser/existir como condição, o nascimento. Do nascimento como
condição, então o envelhecimento e morte, tristeza, lamentação, dor, angústia e
desespero surgem. Essa é a origem de toda essa massa de sofrimento. Mas com o
desaparecimento e cessação sem deixar vestígios dessa mesma ignorância cessam
as formações volitivas. Da cessação das formações volitivas cessa a consciência. Da cessação da
consciência cessa a mentalidade-materialidade (nome e forma). Da cessação da mentalidade-materialidade (nome e forma)
cessam as seis bases dos sentidos. Da cessação das seis bases dos sentidos
cessa o contato. Da cessação do contato cessa a sensação. Da cessação da
sensação cessa o desejo. Da cessação do desejo cessa o apego. Da cessação do
apego cessa o ser/existir. Da cessação do ser/existir cessa o nascimento. Da
cessação do nascimento, então o envelhecimento e morte, tristeza, lamentação,
dor, angústia e desespero, tudo cessa. Essa é a cessação de toda essa massa de
sofrimento.” <b style='mso-bidi-font-weight:normal'><o:p></o:p></b></span></p>


