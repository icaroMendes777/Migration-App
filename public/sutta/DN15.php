<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Com
certeza não, venerável senhor,”</span></p>

<p class=Normal>Por conseguinte, Ananda, devido a isso não é aceitável que
se pressuponha: ‘A sensação não é o meu eu; meu eu não experimenta sensações.’</p>



<p class=Normal>31. “Ananda, aquele que diz ‘A sensação não é o meu eu, mas
o meu eu não está sem a experiência da sensação. Meu eu sente; pois o meu eu
