<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Certa ocasião o Abençoado estava entre os Kurus em uma cidade denominada
Kammasadhamma, então o Venerável Ananda foi até o Abençoado e depois de
cumprimentá-lo sentou a um lado e disse:<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“É
maravilhoso e admirável, venerável senhor, que essa origem dependente seja tão
profunda e difícil de ser vista, no entanto para mim ela parece tão simples,
clara como a luz do dia.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não diga
isso, Ananda! Não diga isso, Ananda! Esta origem dependente é um ensinamento
profundo, difícil de ser visto. [<a href="#N1">1</a>] <a name=R1></a>É por não
entender, não compreender e não penetrar de forma completa este ensinamento que
os seres ficam confusos como um novelo embaraçado, como uma corda embolada cheia de nós, 
como juncos enredados, e não conseguem escapar da transmigração, dos planos de miséria, dos destinos ruins, dos mundos inferiores.[<a href="#N2">2</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2. “Ananda, se alguém for perguntado: ‘O envelhecimento e morte surgem
devido a uma condição específica?’ A resposta deveria ser: ‘Sim.’ Se alguém for
perguntado: ‘Através de qual condição surge o envelhecimento e morte?’ a
resposta deveria ser: ‘Com o nascimento como condição surge o envelhecimento e
morte.’[<a href="#N3">3</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Ananda, se alguém for perguntado: ‘O nascimento surge devido a uma
condição específica?’ A resposta deveria ser: ‘Sim.’ Se alguém for perguntado:
‘Através de qual condição surge o nascimento?’ A resposta deveria ser: ‘Com o
ser/existir como condição surge o nascimento.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ananda, se
alguém for perguntado: ‘O ser/existir surge devido a uma condição específica?’
A resposta deveria ser: ‘Sim.’ Se alguém for perguntado: ‘Através de qual
condição surge o ser/existir?’ A resposta deveria ser: ‘Com o apego como
condição surge o ser/existir.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ananda, se
alguém for perguntado: ‘O apego surge devido a uma condição específica?’ A
resposta deveria ser: ‘Sim.’ Se alguém for perguntado: ‘Através de qual condição
surge o apego?’ A resposta deveria ser: ‘Com o desejo como condição surge o
apego.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ananda, se
alguém for perguntado: ‘O desejo surge devido a uma condição específica?’ A
resposta deveria ser: ‘Sim.’ Se alguém for perguntado: ‘Através de qual condição
surge o desejo?’ A resposta deveria ser: ‘Com a sensação como condição surge o
desejo.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ananda, se
alguém for perguntado: ‘A sensação surge devido a uma condição específica?’ A
resposta deveria ser: ‘Sim.’ Se alguém for perguntado: ‘Através de qual condição
surge a sensação?’ A resposta deveria ser: ‘Com o contato como condição surge a
sensação.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ananda, se
alguém for perguntado: ‘O contato surge devido a uma condição específica?’ A
resposta deveria ser: ‘Sim.’ Se alguém for perguntado: ‘Através de qual
condição surge o contato?’ A resposta deveria ser: ‘Com a
mentalidade-materialidade (nome e forma) como condição surge o contato.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ananda, se
alguém for perguntado: ‘A mentalidade-materialidade (nome e forma) surge devido a uma condição
específica?’ A resposta deveria ser: ‘Sim.’ Se alguém for perguntado: ‘Através
de qual condição surge a mentalidade-materialidade (nome e forma)?’ A resposta deveria ser:
‘Com a consciência como condição surge a mentalidade-materialidade (nome e forma).’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ananda, se
alguém for perguntado: ‘A consciência surge devido a uma condição específica?’
A resposta deveria ser: ‘Sim.’ Se alguém for perguntado: ‘Através de qual
condição surge a consciência?’ A resposta deveria ser: ‘Com a
mentalidade-materialidade (nome e forma) como condição surge a consciência.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3.
“Portanto Ananda, com a mentalidade-materialidade (nome e forma) como condição surge a
consciência; com a consciência como condição surge a mentalidade-materialidade (nome e forma);
com a mentalidade-materialidade (nome e forma) como condição surge o contato; com o contato
como condição surge a sensação; com a sensação como condição surge o desejo;
com o desejo como condição surge o apego; com o apego como condição surge o
ser/existir; com o ser/existir como condição surge o nascimento; e com o
nascimento como condição surgem o envelhecimento e morte, tristeza, lamentação
dor, angústia e desespero. Essa é a origem de toda essa massa de sofrimento.<o:p></o:p></span></p>



<p class=Normal><b><span lang=PT-BR>Envelhecimento e Morte</span></b></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. “Foi
dito: ‘Com o nascimento como condição surge o envelhecimento e morte.’ Isso,
Ananda, deve ser compreendido desta forma: Se não houvesse absolutamente e
completamente nenhum nascimento de qualquer tipo em nenhum lugar – isto é, de
devas no mundo dos devas,  fantasmas famintos,
demônios, seres humanos, quadrúpedes, animais com asas e répteis, cada um no
seu próprio mundo – se não houvesse nenhum nascimento de seres de qualquer tipo
em nenhum mundo, então, com a completa ausência de nascimento, com a cessação
do nascimento, haveria como discernir o envelhecimento e morte?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Com
certeza não, venerável senhor.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
Ananda, essa é a causa, a fonte, a origem e condição para o envelhecimento e
morte, isto é, o nascimento.<o:p></o:p></span></p>



<p class=Normal><b><span lang=PT-BR>Nascimento</span></b></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. “Foi
dito: ‘Com o ser/existir como condição surge o nascimento.’ Isso, Ananda, deve
ser compreendido desta forma: Se não houvesse absolutamente e completamente
nenhum ser/existir de qualquer tipo em nenhum lugar, isto é, não houvesse
ser/existir no reino da esfera sensual, ser/existir no reino da forma,
ou ser/existir no reino sem forma – então, com a completa ausência de
ser/existir, com a cessação do ser/existir, haveria como discernir o
nascimento?” [<a href="#N4">4</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Com certeza não, venerável senhor.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
Ananda, essa é a causa, a fonte, a origem e condição para o nascimento, isto é,
o ser/existir.<o:p></o:p></span></p>



<p class=Normal><b><span lang=PT-BR>Ser/existir</span></b></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. “Foi dito:
‘Com o apego como condição surge o ser/existir.’ Isso, Ananda, deve ser
compreendido desta forma: Se não houvesse absolutamente e completamente nenhum
apego de qualquer tipo em nenhum lugar, isto é, nenhum apego a prazeres
sensuais, apego a idéias, apego a preceitos e rituais ou apego a idéias da existência de um eu – então, com a completa ausência do apego, com a cessação do apego,
haveria como discernir o ser/existir?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Com
certeza não, venerável senhor.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
Ananda, essa é a causa, a fonte, a origem e condição para o ser/existir, isto
é, o apego.<o:p></o:p></span></p>



<p class=Normal><b><span lang=PT-BR>Apego</span></b></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. “Foi
dito: ‘Com o desejo como condição surge o apego.’ Isso, Ananda, deve ser
compreendido desta forma: Se não houvesse absolutamente e completamente nenhum desejo
de qualquer tipo em nenhum lugar, isto é, nenhum desejo por formas visíveis,
desejo por sons, desejo por aromas, desejo por sabores, desejo por tangíveis ou
desejo por objetos mentais – então, com a completa ausência do desejo, com a
cessação do desejo, haveria como discernir o apego?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Com
certeza não, venerável senhor.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
Ananda, essa é a causa, a fonte, a origem e condição para o apego, isto é, o
desejo.<o:p></o:p></span></p>



<p class=Normal><b><span lang=PT-BR>Desejo</span></b></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. “Foi
dito: ‘Com a sensação como condição surge o desejo.’ [<a href="#N5">5</a>] <a
name=R5></a>Isso, Ananda, deve ser compreendido desta forma: Se não houvesse
absolutamente e completamente nenhuma sensação de qualquer tipo em nenhum
lugar, isto é, nenhuma sensação originada do contato no olho, nenhuma sensação
originada do contato no ouvido, nenhuma sensação originada do contato no nariz,
nenhuma sensação originada do contato na língua, nenhuma sensação originada do
contato no corpo, nenhuma sensação originada do contato na mente – então, com a
completa ausência da sensação, com a cessação da sensação, haveria como
discernir o desejo?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Com
certeza não, venerável senhor.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
Ananda, essa é a causa, a fonte, a origem e condição para o desejo, isto é, a
sensação. <o:p></o:p></span></p>



<p class=Normal><b><span lang=PT-BR>Dependente do Desejo</span></b></p>



<p class=Normal><a name=T9></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>9. “Então, Ananda, na dependência do desejo surge o apego; na
dependência do apego surge a busca; na dependência da busca surge o ganho; na
dependência do ganho surge a decisão; na dependência da decisão surge o desejo
e cobiça; na dependência do desejo e cobiça surge o apego; na dependência do
apego surge a posse; na dependência da posse surge a mesquinharia; na
dependência da mesquinharia surge a salvaguarda; e devido à salvaguarda, muitos
fenômenos ruins e prejudiciais surgem – tomar clavas e armas, conflitos, brigas
e disputas, linguagem ofensiva, difamação e falsidades.[<a href="#N6">6</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>10. “Foi dito: ‘Devido à salvaguarda, muitos fenômenos ruins e
prejudiciais surgem – tomar clavas e armas, conflitos, brigas e disputas,
linguagem ofensiva, difamação e falsidades.’ Isso, Ananda, deve ser
compreendido desta forma: Se não houvesse absolutamente e completamente nenhuma
salvaguarda de qualquer tipo em nenhum lugar, então, com a completa ausência de
salvaguardas, com a cessação de salvaguardas, aqueles vários fenômenos ruins e
prejudiciais surgiriam?<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Com
certeza não, venerável senhor.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
Ananda, essa é a causa, a fonte, a origem e condição para aqueles vários
fenômenos ruins e prejudiciais, isto é, as salvaguardas.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. “Foi
dito: ‘Na dependência da mesquinharia surge a salvaguarda,’ Isso, Ananda, deve
ser compreendido desta forma: Se não houvesse absolutamente e completamente
nenhuma mesquinharia de qualquer tipo em nenhum lugar, então, com a completa
ausência da mesquinharia, com a cessação da mesquinharia, haveria como
discernir a salvaguarda?<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Com
certeza não, venerável senhor.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
Ananda, essa é a causa, a fonte, a origem e condição para a salvaguarda, isto
é, a mesquinharia.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. “Foi
dito: ‘Na dependência da posse surge a mesquinharia’ Isso, Ananda, deve ser
compreendido desta forma: Se não houvesse absolutamente e completamente nenhuma
posse de qualquer tipo em nenhum lugar, então, com a completa ausência da
posse, com a cessação da posse, haveria como discernir a mesquinharia?<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Com
certeza não, venerável senhor.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então, Ananda,
essa é a causa, a fonte, a origem e condição para a mesquinharia, isto é, a
posse. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. “Foi
dito: ‘Na dependência do apego surge a posse,’ Isso, Ananda, deve ser
compreendido desta forma: Se não houvesse absolutamente e completamente nenhum
apego de qualquer tipo em nenhum lugar, então, com a completa ausência do
apego, com a cessação do apego, haveria como discernir a posse?<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Com
certeza não, venerável senhor.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
Ananda, essa é a causa, a fonte, a origem e condição para a posse, isto é, o apego.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14. “Foi
dito: ‘Na dependência do desejo e cobiça surge o apego,’ Isso, Ananda, deve ser
compreendido desta forma: Se não houvesse absolutamente e completamente nenhum
desejo e cobiça de qualquer tipo em nenhum lugar, então, com a completa ausência
do desejo e cobiça, com a cessação do desejo e cobiça, haveria como discernir o
apego?<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Com
certeza não, venerável senhor.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
Ananda, essa é a causa, a fonte, a origem e condição para o apego, isto é, o
desejo e cobiça.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. “Foi
dito: ‘Na dependência da decisão surge o desejo e cobiça.’ Isso, Ananda, deve
ser compreendido desta forma: Se não houvesse absolutamente e completamente
nenhuma decisão de qualquer tipo em nenhum lugar, então, com a completa
ausência da decisão, com a cessação da decisão, haveria como discernir o desejo
e cobiça?<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Com
certeza não, venerável senhor.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
Ananda, essa é a causa, a fonte, a origem e condição para o desejo e cobiça,
isto é, a decisão.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16. “Foi
dito: ‘Na dependência do ganho surge a decisão,’ Isso, Ananda, deve ser
compreendido desta forma: Se não houvesse absolutamente e completamente nenhum
ganho de qualquer tipo em nenhum lugar, então, com a completa ausência do
ganho, com a cessação do ganho, haveria como discernir a decisão?<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Com
certeza não, venerável senhor.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
Ananda, essa é a causa, a fonte, a origem e condição para a decisão, isto é, o
ganho.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17. “Foi
dito: ‘Na dependência da busca surge o ganho,’ Isso, Ananda, deve ser
compreendido desta forma: Se não houvesse absolutamente e completamente nenhuma
busca de qualquer tipo em nenhum lugar, então, com a completa ausência da
busca, com a cessação da busca, haveria como discernir o ganho?<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Com
certeza não, venerável senhor.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
Ananda, essa é a causa, a fonte, a origem e condição para o ganho, isto é, a
busca.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18. “Foi
dito: ‘Na dependência do desejo surge a busca,’ Isso, Ananda, deve ser
compreendido desta forma: Se não houvesse absolutamente e completamente nenhum
desejo de qualquer tipo em nenhum lugar – isto é, nenhum desejo por prazeres
sensuais, desejo por existir ou desejo por não existir - então, com a completa
ausência do desejo, com a cessação do desejo, haveria como discernir a busca?<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Com
certeza não, venerável senhor.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
Ananda, essa é a causa, a fonte, a origem e condição para a busca, isto é, o
desejo.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Assim,
Ananda, esses dois fenômenos, sendo uma dualidade, convergem para uma unidade
na sensação.[<a href="#N7">7</a>] <o:p></o:p></span></p>


<p class=Normal><b><a name=R7></a><span lang=PT-BR>Sensação</span></b></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>19. “Foi
dito: ‘Com o contato como condição surge a sensação.’ [<a href="#N8">8</a>] <a
name=R8></a>Isso, Ananda, deve ser compreendido desta forma: Se não houvesse
absolutamente e completamente nenhum contato de qualquer tipo em nenhum lugar,
isto é, nenhum contato no olho, nenhum contato no ouvido, nenhum contato no
