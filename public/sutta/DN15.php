na qual o ciclo gira: tendo o conhecimento direto disso o bhikkhu está libertado. Dizer que - 
'um bhikkhu que está libertado, tendo o 
conhecimento direto disso, não sabe e não vê, é a sua opinião' – não seria apropriado.<o:p></o:p></span></p>


<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>As sete
