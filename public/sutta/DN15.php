ocasião em que alguém experimenta uma sensação nem prazerosa, nem dolorosa,
ele, naquela mesma ocasião não experimenta uma sensação prazerosa, ou uma
sensação dolorosa; naquela ocasião ele experimenta apenas uma sensação nem
prazerosa, nem dolorosa.</p>



<p class=Normal>29. “Ananda, a sensação prazerosa é impermanente,
condicionada, com origem dependente, sujeita à destruição, a decair,
desaparecer e cessar. A sensação dolorosa é impermanente, condicionada, com
origem dependente, sujeita à destruição, a decair, desaparecer e cessar. A
sensação nem prazerosa, nem dolorosa é impermanente, condicionada, com origem
dependente, sujeita à destruição, a decair, desaparecer e cessar.</p>

<p class=Normal>“Se, ao experimentar uma sensação prazerosa, alguém pensa:
‘Este é o meu eu,’ então com a cessação daquela sensação prazerosa ele
pensaria: ‘Meu eu desapareceu.’ Se, ao experimentar uma sensação dolorosa,
alguém pensa: ‘Este é o meu eu,’ então com a cessação daquela sensação dolorosa
ele pensaria: ‘Meu eu desapareceu.’ Se, ao experimentar uma sensação nem
prazerosa, nem dolorosa, alguém pensa: ‘Este é o meu eu,’ então com a cessação
daquela sensação nem prazerosa, nem dolorosa<span style='mso-spacerun:yes'> 
</span>ele pensaria: ‘Meu eu desapareceu.</p>

<p class=Normal>“Assim aquele que diz ‘A sensação é o meu eu’ pressupõe como
eu algo que, até mesmo no aqui e agora, é<span style='mso-spacerun:yes'> 
</span>impermanente, uma mistura de prazer e dor e sujeito ao surgimento e à
cessação. Por conseguinte, Ananda, devido a isso não é aceitável que se
pressuponha: ‘A sensação é o meu eu.’</p>



<p class=Normal>30. “Ananda, aquele que diz ‘A sensação não é o meu eu; meu
eu não experimenta sensações’ – ele deveria ser perguntado: ‘Amigo, onde não há
