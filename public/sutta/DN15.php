<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Com
certeza não, venerável senhor,”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ananda,
quando um bhikkhu – tendo compreendido como na verdade é a origem, a cessação,
a gratificação, o perigo e a escapatória em relação a essas sete estações da
consciência e as duas bases – está libertado através do desapego, então ele é
chamado de um bhikkhu libertado através da sabedoria.<o:p></o:p></span></p>


<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>As oito
libertações</span></b><span lang=PT-BR style='mso-ansi-language:PT-BR'> [</span><a
href="#N15"><span lang=PT-BR style='mso-ansi-language:PT-BR'>15</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>


<p class=Normal><a name=R15></a><a name=T35></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>35. “Ananda, existem essas oito libertações. Quais
são as oito? <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Possuindo
forma, ele vê a forma: essa é a primeira libertação. Não percebendo a
forma no interior, ele vê a forma no exterior: essa é a segunda libertação. Ele
está decidido apenas pelo belo: essa é a terceira libertação. Com a completa
superação das percepções da forma, com o desaparecimento das percepções do
contato sensorial, sem dar atenção às percepções da diversidade, consciente que
o ‘espaço é infinito,’ ele entra e permanece na base do espaço infinito: essa é
a quarta libertação. Com a completa superação da base do espaço infinito,
consciente de que a ‘consciência é infinita,' ele entra e permanece na base da
consciência infinita: essa é a quinta libertação. Com a completa superação da
base da consciência infinita, consciente de que ‘não há nada,’ ele entra e
permanece na base do nada: essa é a sexta libertação. Com a completa superação
da base do nada, ele entra e permanece na base da nem percepção, nem não
percepção: essa é a sétima libertação. Com a completa superação da base da nem
percepção, nem não percepção, ele entra e permanece na cessação da sensação e
percepção: essa é a oitava libertação.”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>36.
“Ananda, quando um bhikkhu alcança essas oito libertações na seqüência para
diante, na seqüência para trás e em ambas as seqüências, para diante e para
trás; quando ele as alcança e emerge delas quando quiser, do modo que quiser e
por quanto tempo quiser e quando, com a eliminação das impurezas mentais, ele
permanece num estado livre de impurezas com a libertação da mente e a
libertação através da sabedoria, tendo conhecido e manifestado isso para si mesmo no
aqui e agora, então ele é chamado de um bhikkhu que está libertado de ambas as
formas. E, Ananda, não existe outra libertação de ambas as formas mais elevada
ou mais sublime do que essa.”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isto foi o
que o Abençoado disse. O Venerável Ananda ficou satisfeito e contente com as
palavras do Abençoado.<o:p></o:p></span></p>


