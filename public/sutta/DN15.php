<p class=Normal>“Ananda, na ocasião em que alguém experimenta uma sensação
prazerosa, ele, naquela mesma ocasião, não experimenta uma sensação dolorosa, ou
uma sensação nem prazerosa, nem dolorosa; naquela ocasião ele experimenta
