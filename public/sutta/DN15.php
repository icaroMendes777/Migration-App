<p class=Normal ><a name=N1></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>[1] DA explica que existem quatro aspectos
pelos quais a origem dependente é caracterizada como profunda: devido à
profundidade do significado, profundidade do fenômeno, profundidade do
ensinamento e profundidade de penetração. O primeiro par de profundidades
(significado e fenômeno) se aplica de modo oposto a cada par dos fatores ou
elos da cadeia de origem dependente. A “profundidade do significado” se aplica
à ligação vista da perspectiva do efeito voltando-se para trás, para a sua
condição; a “profundidade do fenômeno”, à ligação vista da perspectiva da
condição voltando-se para frente, para o seu efeito. Cada perspectiva destaca
um tipo diferente de profundidade. No primeiro caso, a profundidade está na
invariável dependência da condição em relação ao efeito: o efeito sempre surge
e se mantém com o suporte da sua condição, nunca com a ausência desta. No
último caso, a profundidade consiste na eficácia da condição: como esta exerce
o seu papel causal em relação ao seu efeito. A “profundidade do ensinamento” se
refere à diversidade de métodos que o Buda usou para expor a origem dependente.
A “profundidade de penetração” se refere à penetração da natureza última dos
fenômenos ou <i>dhammas</i>. [<a href="#R1">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal ><a name=N2></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>[2] O restante do sutta de acordo com DA é um
desenvolvimento das duas afirmações feitas pelo Buda, isto é, “esta origem
dependente é profunda” e “os seres ficam confusos como um novelo
embaraçado....” A primeira conduz ao relato detalhado da origem dependente
(versos 2 a 22); a última conduz à investigação metódica das idéias quanto à
existência de um eu (versos 23 a 32). [<a href="#R2">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal ><a name=N3></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>[3] O Buda expõe a seqüência das condições em
ordem inversa. Sob o ponto de vista conceitual pode não haver diferença no
significado, caso a seqüência seja apresentada na seqüência tradicional ou
inversa. Mas a exposição do Dhamma pelo Buda tem um outro propósito além de
comunicar o conceito básico. Esse objetivo é a motivação: estimular a vontade e
fazer com que surja o ímpeto em direção aos objetivos do ensinamento, a
cessação do sofrimento. A apresentação em ordem inversa satisfaz esse objetivo
de modo muito mais eficaz que a apresentação tradicional. A apresentação em
ordem inversa expõe o ciclo sob o ponto de vista existencial. De cara, o
ouvinte é confrontado com o problema da sua existência (a morte como a epítome
do sofrimento inerente à existência) para depois conduzí-lo passo a passo ao
longo da cadeia de condições que estão por trás daquele problema. [<a href="#R3">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal ><a name=N4></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>[4] Em geral esses reinos denotam os planos de
existência, os três níveis no samsara nos quais pode ocorrer o renascimento.
Mas como o renascimento em cada um desses reinos ocorre de acordo com um certo
tipo de <i>kamma</i>, o termo “ser/existir”, de acordo com DA, representa o
kamma que conduz ao renascimento naquele reino em particular. [<a href="#R4">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal ><a name=N5></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>[5] O desejo sensual e o apego a prazeres
sensuais significam o mesmo fator mental, cobiça (<i>lobha</i>) em diferentes
estágios de intensidade. O desejo também faz surgir o apego por idéias. Em
geral a idéia que favorece a compulsão mais dominante. Assim por exemplo o
desejo pela existência pode levar à crença na imortalidade da alma, o desejo
por não existir pode levar à teoria da aniquilação completa com a morte.<span
style='mso-spacerun:yes'>  </span>[<a href="#R5">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal ><a name=N6></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>[6] Na seqüência normal, imediatamente depois
de indicar a sensação como condição para o desejo, o Buda menciona o contato
como condição para a sensação. Aqui, no entanto, ele introduz uma variação. Da sensação
ele regressa ao desejo e em seguida extrai do desejo uma nova série de nove
fatores, cada um surgindo na dependência do anterior. O desejo leva à <i>busca</i>
dos objetos desejados e no final por meio da busca eles são obtidos. Quando os
objetos são obtidos a pessoa toma <i>decisões</i> com relação a eles: o que é
meu e o que é seu, o que possui valor e o que não possui, o que irei guardar e
o que irei desfrutar. Devido a essas decisões, pensamentos de <i>desejo e
cobiça</i> surgem. A pessoa desenvolve <i>apego</i> pelos objetos, adota uma
atitude <i>possessiva</i> com relação a eles e acaba se tornando <i>mesquinha</i>,
recusando compartí-los com outros. Encarando os outros com medo e suspeita, a
pessoa busca como <i>salvaguardar</i> os seus objetos. Quando esse tipo de
cobiça e medo se difunde amplamente é necessário apenas uma mínima provocação
para irromper em violência, conflitos, etc. Esse resumo esclarece o propósito
dessa digressão: é para mostrar que o princípio da origem dependente pode ser
empregado para compreender as origens da desordem social da mesma forma como
pode ser empregado para compreender o sofrimento individual. DA rotula esses
dois aspectos do desejo como “desejo que se encontra na raiz de todo o samsara”
e “desejo obsessivo.” Mas deve ser observado que essas duas expressões não
denotam distintos tipos de desejo; elas apenas apontam ângulos distintos por
meio dos quais qualquer exemplo de desejo pode ser encarado. Pois o desejo que
resulta em desordem e violência é o mesmo que gera<span
style='mso-spacerun:yes'>  </span><i>kamma</i> inábil e que mantém o ciclo do <i>samsara</i>,
enquanto que o desejo pelo prazer e por ser/existir que mantém <i>samsara</i>
também conduz à ruptura da harmonia social. [<a href="#R6">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal ><a name=N7></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>[7] O Buda se refere aos dois aspectos do
desejo descritos na nota 6. Ambos os tipos de desejo encontram a sua condição
na sensação. A sensação por seu lado tem origem no contato (<i>phassa</i>).
Contato é o encontro da consciência com um objeto por meio da faculdade do
sentido. [<a href="#R7">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal ><a name=N8></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>[8] Sensação (<i>vedana</i>) é o caráter emotivo
das experiências – a sensação de prazer, dor ou neutra – que ocorre em cada
ocasião de uma experiência através de qualquer um dos seis meios dos sentidos
(ou seis bases internas). [<a href="#R8">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal ><a name=N9></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>[9] Na exposição tradicional do ciclo da origem
dependente a seqüência passa do contato para as seis bases. Neste sutta, no
entanto, o Buda evita as seis bases completamente e volta um passo para mostrar
a mentalidade-materialidade (nome e forma) como condição para o contato.
Mentalidade-materialidade (nome e forma) é um termo composto, que em geral é usado nos suttas
para descrever o organismo psíquico-físico excluindo a consciência, que serve
como condição desta. Com menos freqüência nos suttas, o escopo do termo é
ampliado adicionando também as bases dos sentidos externas. Neste caso, a
mentalidade-materialidade (nome e forma) torna-se a experiência completa que se apresenta para
a consciência, o organismo senciente juntamente com os objetos externos.
“Contato de designação” (<i>adhivacanasamphassa) </i>e “contato de colisão” <i>(patighasamphassa)
</i>são dois termos peculiares a este sutta. DA identifica o primeiro com o
contato na mente e o último com o contato nos cinco meios dos sentidos. O que é
significativo é o argumento do Buda demonstrando como a
mentalidade-materialidade (nome e forma) é a condição para o contato. O Buda diz que é
impossível o contato de designação no corpo material <i>(rupakaya) </i>quando
aquelas qualidades peculiares ao corpo mental <i>(namakaya) </i>estiverem
ausentes e é impossível o contato de colisão no corpo mental quando aquelas
qualidades peculiares ao corpo material estiverem ausentes. Assim, cada tipo de
contato depende de ambos, do corpo mental e do corpo material. Neste último, o
significado de corpo abrange apenas o organismo senciente e não inclui os
objetos externos. O argumento indica o papel especial do contato como o ponto
de encontro da mente com o mundo. O contato requer uma base externa (o objeto),
uma base interna (as faculdades dos sentidos) e a consciência. Mas esse
processo é como um caminho de duas mãos, pois o contato pode ocorrer com a
mente movendo-se para fora em direção ao mundo ou o mundo<span
style='mso-spacerun:yes'>  </span>movendo-se para dentro em direção à mente. O
movimento para fora ocorre nas situações da consciência na mente, nas quais as
atividades conceituais e volitivas prevalecem; o movimento para dentro ocorre
nas ocasiões da consciência nos sentidos, quando a relação da mente com os
objetos é de uma recepção passiva. O movimento para fora tem início com a
designação, o ato de dar nomes. Ao atribuir nomes, a mente organiza a matéria
bruta da experiência num quadro coerente do mundo. O movimento para dentro
ocorre quando há um impacto ou colisão do objeto com a faculdade do sentido. Se
o impacto for forte o suficiente, surge a consciência naquele meio do sentido.
Os dois termos, designação e colisão indicam um padrão oscilatório das
experiências entre as fases de recepção e resposta. A fase receptiva
experimenta a maturação do fluxo de kamma do passado, ela é representada aqui
pela colisão que gera a consciência nos sentidos. A fase de resposta envolve a
formação de novo kamma, ela é representada pela designação que conduz à ação.
Em síntese, sem os fatores mentais não poderia haver o contato de designação e
sem o corpo material com as suas faculdades dos sentidos não poderia haver
contato de colisão. Portanto, na ausência tanto do corpo mental<span
style='mso-spacerun:yes'>  </span>como do corpo material não haveria como
discernir o contato. A conclusão que segue é que o contato depende da
mentalidade-materialidade (nome e forma), por conseguinte, a mentalidade-materialidade (nome e forma) é a
condição para o contato.[<a href="#R9">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal ><a name=N10></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>[10] Estes dois últimos versos conduzem a
investigação da origem dependente ao seu clímax ao revelar um “vórtice oculto”
que está por trás de todo o processo de ser/existir no <i>samsara</i>. Esse
vórtice oculto é a condicionalidade recíproca da consciência e da
mentalidade-materialidade (nome e forma). Inicialmente, o Buda estabelece a consciência como
condição específica para a mentalidade-materialidade (nome e forma) ao demonstrar que ela é
indispensável para esta última em quatro momentos distintos: na concepção,
durante a gestação, ao nascer e durante o transcurso da vida (verso 21). A
descrição da consciência se estabelecendo no ventre da mãe é metafórica; isso
não deve ser tomado no sentido literal, implicando que a consciência é uma
entidade que transmigra de uma vida para outra. A consciência ocorre sob a
forma de um processo, uma série de atos de cognição que surgem e desaparecem de
acordo com causas e condições. Isso indica que no momento da concepção a
consciência não surge outra vez, espontaneamente, sem antecedentes, mas que
ocorre como um dos momentos de um contínuo da consciência, que está
prosseguindo de forma ininterrupta de uma vida para outra, desde um princípio
que não pode ser determinado. No verso 22, o Buda inverte a afirmação anterior
e pronuncia que ‘Com a mentalidade-materialidade (nome e forma) como condição surge a
consciência.’ Tal como o feto não pode se formar a não ser que a consciência
se estabeleça no ventre, da mesma forma a consciência não pode dar início a uma
nova existência no ventre, a menos que se estabeleça na
mentalidade-materialidade (nome e forma). Além disso, a consciência precisa da
mentalidade-materialidade (nome e forma) não somente na concepção, mas ao longo de toda a
vida. A consciência depende de um corpo vivo funcionando com o cérebro, sistema
nervoso e faculdades dos sentidos. Ela também depende do corpo mental, já que
não pode haver a cognição de um objeto sem as funções mais especializadas
desempenhadas pelo contato, sensação, percepção, volição, atenção e todos os
demais fatores mentais. Assim, presas nesse vórtice de inter-relações a
consciência e a mentalidade-materialidade (nome e forma) se suportam mutuamente, se alimentam
mutuamente e se impulsionam mutuamente, gerando a partir da sua união,
infectadas pelo desejo e<span style='mso-spacerun:yes'>  </span>ignorância,
toda a série de fenômenos condicionados que culminam com o envelhecimento e
morte. Esse é o propósito das palavras do Buda ao final do verso 22, “Essa é a extensão, Ananda, 
na qual há o nascimento, envelhecimento, morte, falecimento e renascimento...” “Essa extensão” 
significa “por este tanto,” “não
por meio de qualquer outra coisa que não seja isto.” [<a href="#R10">Retorna</a>]<span
style='mso-spacerun:yes'>  </span><o:p></o:p></span></p>


<p class=Normal ><a name=N11></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>[11] De acordo com DA, designação, <i>(adhivacana),</i>
linguagem, <i>(nirutti), </i>e conceituação, <i>(paññatti), </i>são quase
sinônimos, significando, com mínimas diferenças de nuance, enunciados verbais
que expressam um significado. A extensão é a mesma nos três casos,
isto é, os cinco agregados, neste caso a mentalidade-materialidade (nome e forma) mais a
consciência. Portanto, este trecho pode ser interpretado como uma referência,
de forma elíptica, à relação entre os conceitos, linguagem e realidade.
Enquanto os agregados estiverem envoltos pela ignorância, eles são tomados como
base para a concepção de noções deludidas de “meu”, “eu sou” e “meu eu”. Mas
isso não é tudo que a inter-relação entre a consciência e a
mentalidade-materialidade (nome e forma) permite. O Buda diz que a sabedoria também é
possível. A extensão para a sabedoria são os próprios cinco
agregados, que quando examinados com atenção plena e plena consciência, se
transformam no terreno fértil para o crescimento da sabedoria. A sabedoria
opera com o mesmo conjunto de referenciais que a conceituação deludida – os
cinco agregados. [<a href="#R11">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal ><a name=N12></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>[12] Nesta seção o Buda parece estar desviando
a discussão para um novo tópico que aparentemente não está relacionado com a
discussão precedente. DA explica a direção do discurso indicando que esta nova
seção se refere ao enunciado original do Buda “que os seres ficam confusos como
um novelo embaraçado...” O propósito é de elucidar esse enunciado identificando
os enredos e mostrando como esse processo ocorre. Portanto a discussão ainda
diz respeito à estrutura causal do ciclo, só que agora essa estrutura está
sendo abordada por um outro ângulo. O exame das idéias de um ‘eu’ foca num
fator específico na seqüência das condições, isto é, o apego sob a forma de ‘idéias da existência de um eu.’ Nessa forma, o apego desempenha um papel de
importância crítica pois representa o ponto em que<span
style='mso-spacerun:yes'>  </span>a ignorância e o desejo adquirem uma
justificativa intelectual com a criação de uma visão conceitual do eu,
protegida por uma fachada de racionalidade.[<a href="#R12">Retorna</a>] <o:p></o:p></span></p>


<p class=Normal ><a name=N13></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>[13] Tanto as descrições do eu como as
pressuposições do eu são idéias, mas as pressuposições ocupam um nível mais
rudimentar na escala de subjetividade. As descrições do eu envolvem um alto
grau de reflexão: com teorias sobre o eu, especulações sobre o seu destino,
argumentos bem ponderados e provas. As pressuposições do eu não são totalmente
desprovidas de reflexão, mas é um tipo de reflexão menos elaborada e refinada
do que aquela das descrições. [<a href="#R13">Retorna</a>]<o:p></o:p></span></p>

