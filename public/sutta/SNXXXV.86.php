<p class=Normal> Sentado a um lado o venerável Ananda disse para o Abençoado: “Venerável senhor, seria bom se o Abençoado me ensinasse o Dhamma de forma resumida, de modo que, tendo ouvido o Dhamma do Abençoado eu possa permanecer só, isolado, diligente, ardente e decidido.”</p>

<p class=Normal>O que você pensa, Ananda, o olho é permanente ou impermanente?” – “Impermanente, venerável senhor.” – “E aquilo que é impermanente é sofrimento ou felicidade?” – “Sofrimento, venerável senhor.” – “E é adequado considerar o que é impermanente, sofrimento, sujeito a mudanças como: ‘Isso é meu. Isso sou eu. Isso é o meu eu’?” – “Não, venerável senhor.”</p>

<p class=Normal> “As formas são permanentes ou impermanentes? ... A consciência no olho ... O contato no olho ... Qualquer sensação que surja tendo o contato no olho como condição – quer seja prazerosa, dolorosa ou nem prazerosa, nem dolorosa – é permanente ou impermanente? ...</p>

<p class=Normal> “O ouvido é permanente ou impermanente? ... A mente ... Qualquer sensação que surja tendo o contato na mente como condição – quer seja prazerosa, dolorosa ou nem prazerosa, nem dolorosa – é permanente ou impermanente?” – “Impermanente, venerável senhor.” – “E aquilo que é impermanente é sofrimento ou felicidade?” – “Sofrimento, venerável senhor.” – “E é adequado considerar o que é impermanente, sofrimento, sujeito a mudanças como: ‘Isso é meu. Isso sou eu. Isso é o meu eu’?” – “Não, venerável senhor.”</p>

<p class=Normal> “Vendo desse modo, o nobre discípulo bem instruído se desencanta com o olho, com as formas, com a consciência no olho, com o contato no olho, com qualquer sensação que surja tendo o contato no olho como condição – quer seja prazerosa, dolorosa ou nem prazerosa, nem dolorosa. Ele se desencanta como o ouvido ... com a mente ... com qualquer sensação que surja tendo o contato na mente como condição ... Desencantado ele se torna desapegado. Através do desapego a sua mente é libertada. Quando ela está libertada surge o conhecimento: ‘Libertada.’ Ele compreende que: ‘O nascimento foi destruído, a vida santa foi vivida, o que deveria ser feito foi feito, não há mais vir a ser a nenhum estado.’”</p>

