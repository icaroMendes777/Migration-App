<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
há o êxtase mundano, há o êxtase não mundano e há um êxtase não mundano ainda
maior. Há a felicidade <i><span style="mso-spacerun: yes"> </span></i>mundana,
há a felicidade não mundana e há uma felicidade não mundana ainda maior. Há a
equanimidade mundana, há a equanimidade não mundana e há uma equanimidade não
mundana ainda maior. Há a libertação mundana, há a libertação não mundana e há
uma libertação não mundana ainda maior. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que, bhikkhus, é o
êxtase mundano? Existem esses cinco elementos do prazer sensual: formas
conscientizadas através do olho que são desejáveis, agradáveis e fáceis de serem
gostadas, conectadas com o desejo sensual e que provocam a cobiça. Sons
conscientizados através do ouvido … aromas conscientizados através do nariz … sabores
conscientizados através da língua … tangíveis conscientizados através do corpo que são
desejáveis, agradáveis e fáceis de serem gostados, conectados com o desejo
sensual e que provocam a cobiça. É o êxtase que surge na dependência desses
cinco elementos do prazer sensual que é chamado de ‘êxtase mundano.’ <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que, bhikkhus, é o
êxtase não mundano?<span style="mso-spacerun: yes">  </span>Afastado dos
prazeres sensuais, afastado das qualidades não hábeis, um bhikkhu entra e
permanece no primeiro jhana, que é caracterizado pelo pensamento aplicado e
sustentado, com o êxtase e felicidade nascidos do afastamento. Abandonando o
pensamento aplicado e sustentado, um bhikkhu entra e permanece no segundo
jhana, que é caracterizado pela segurança interna e perfeita unicidade da mente, sem o pensamento aplicado e sustentado, com o êxtase e felicidade nascidos da concentração. Isso é chamado de ‘êxtase não mundano.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que, bhikkhus, é o
êxtase não mundano ainda maior? Quando um bhikkhu que se libertou das impurezas
revisa a sua mente livre da cobiça, livre da raiva, livre da delusão, então
surge o êxtase. Isso é chamado de ‘êxtase não mundano ainda maior.’ <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que, bhikkhus, é a
felicidade mundana? Existem esses cinco elementos do prazer sensual:<span
style="mso-spacerun: yes">  </span>formas conscientizadas através do olho... sons
conscientizados através do ouvido…aromas conscientizados através do nariz…sabores
conscientizados através da língua…tangíveis conscientizados através do corpo que são
desejáveis, agradáveis e fáceis de serem gostados, conectados com o desejo
sensual e que provocam a cobiça. É a felicidade que surge na dependência desses
cinco elementos do prazer sensual que é chamada de ‘felicidade mundana.’ <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que, bhikkhus, é a
felicidade não mundana? Afastado dos prazeres sensuais, afastado das qualidades
não hábeis, um bhikkhu entra e permanece no primeiro jhana ... Abandonando o
pensamento aplicado e sustentado, um bhikkhu entra e permanece no segundo
jhana ... Abandonando o êxtase, um bhikkhu entra e permanece no terceiro jhana que é caracterizado pela felicidade sem o êxtase, acompanhada pela atenção plena, plena consciência e equanimidade, acerca do qual os nobres declaram: ‘Ele permanece numa estada feliz, equânime e plenamente atento.’ Isso é
chamado de ‘felicidade não mundana.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que, bhikkhus, é a felicidade
não mundana ainda maior? Quando um bhikkhu que se libertou das impurezas revisa
a sua mente livre da cobiça, livre da raiva, livre da delusão, então surge a
felicidade. Isso é chamado de ‘felicidade não mundana ainda maior.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que, bhikkhus, é a
equanimidade mundana? Existem esses cinco elementos do prazer sensual: formas
conscientizadas através do olho... tangíveis conscientizados através do corpo que são
desejáveis, agradáveis e fáceis de serem gostados, conectados com o desejo
sensual e que provocam a cobiça. É a equanimidade que surge na dependência
desses cinco elementos do prazer sensual que é chamada de ‘equanimidade
mundana.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que, bhikkhus, é a
equanimidade não mundana?<span style="mso-spacerun: yes">  </span>Com o
o completo desaparecimento da felicidade, um bhikkhu entra e permanece no quarto jhana, que possui nem felicidade nem sofrimento, com a atenção plena e a equanimidade purificadas. Isso é chamado de ‘equanimidade não mundana.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que, bhikkhus, é a
equanimidade não mundana ainda maior? Quando um bhikkhu que se libertou das
impurezas revisa a sua mente livre da cobiça, livre da raiva, livre da delusão,
então surge a equanimidade. Isso é chamado de ‘equanimidade não mundana ainda
maior.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que, bhikkhus, é a
libertação mundana? A libertação conectada com o reino da forma. O que
é a libertação não mundana? A libertação conectada com o reino sem forma. E o que é a libertação não mundana ainda maior? Quando um bhikkhu
que se libertou das impurezas revisa a sua mente livre da cobiça, livre da
raiva, livre da delusão, nisso surge a libertação. Essa é chamada a libertação
não mundana ainda maior.” <o:p></o:p></span></p>



