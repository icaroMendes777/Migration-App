<p class=Normal> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: "Bhikkhus, aqueles bhikkhus exitosos na virtude, exitosos na concentração, exitosos na sabedoria, exitosos na libertação, exitosos no conhecimento e visão da libertação, que são conselheiros, instrutores e demonstradores, que podem estimular, inspirar, e encorajar, que são mestres competentes do verdadeiro Dhamma - ver esses bhikkhus é de grande ajuda, eu digo; ouvir esses bhikkhus, aproximar-se deles; acompanhá-los, lembrar-se deles, e seguir o seu exemplo abandonando a vida em família e seguindo a vida santa é de grande ajuda. Por qual razão?</p>

<p class=Normal> "Ao seguir esses bhikkhus, associando-se a eles e acompanhando-os, o agregado da virtude ainda incompleto será completo através do desenvolvimento, o agregado da concentração ainda incompleto será completo através do desenvolvimento, o agregado da sabedoria ainda incompleto será completo através do desenvolvimento, o agregado da libertação ainda incompleto será completo através do desenvolvimento, o agregado do conhecimento e visão da libertação ainda incompleto será completo através do desenvolvimento. Bhikkhus, eles são chamados: mestres, líderes de caravanas, que abandonaram os defeitos, dissipadores da escuridão, que trazem a luz, emitem luminosidade, conduzem a tocha, trazem a iluminação, nobres, possuidores de visão." </p>

<p class=Normal style='margin-left:.75in'> Para aqueles que têm conhecimento<br>
esse é um estado que traz alegria -<br>
viver a vida no Dhamma<br>
sob um nobre com a mente perfeita. </p>

<p class=Normal style='margin-left:.75in'> Eles esclarecem o verdadeiro Dhamma, <br>
brilhando e iluminando, <br>
trazem a luz, sábios heróicos, <br>
dotados de visão, dissipando os defeitos.</p>

<p class=Normal style='margin-left:.75in'>Tendo ouvido os seus ensinamentos, <br>
o sábio com a compreensão perfeita, <br>
tendo o conhecimento direto do fim do nascimento,<br>
não terá mais a renovação do ser/existir.</p>



