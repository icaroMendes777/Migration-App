<p class=Normal style='margin-left:.75in'>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em certa
ocasião, o Abençoado estava entre os Sakyas em Kapilavatthu, no Parque de
Nigrodha. Agora, naquela ocasião um novo salão para assembléias havia sido
construído recentemente para os Sakyas de Kapilavatthu e ele ainda não havia
sido habitado de modo nenhum por nenhum contemplativo ou brâmane ou ser humano.
Então os Sakyas de Kapilavatthu foram até o Abençoado. Depois de cumprimentá-lo
eles sentaram a um lado e disseram:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, um novo salão para assembléias foi recentemente construído para os
Sakyas de Kapilavatthu e ele ainda não foi habitado de modo nenhum por nenhum
contemplativo ou brâmane ou ser humano. Venerável senhor, que o Abençoado seja
o primeiro a usá-lo. Depois que o Abençoado tiver primeiro usado o salão, então
os Sakyas de Kapilavatthu irão usá-lo. Isso será para a felicidade e bem-estar
deles por muito tempo.” [<a href="#N1">1</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>O Abençoado concordou em silêncio. Então, quando eles viram que ele
havia concordado, eles levantaram dos seus assentos e depois de homenagear o
Abençoado, mantendo-o à sua direita, foram até o salão de assembléias. Eles
cobriram o salão completamente com coberturas e prepararam assentos, e eles
arranjaram um grande jarro com água e penduraram uma lamparina de azeite. Então
eles foram até o Abençoado e depois de cumprimentá-lo, ficaram em pé a um lado
e disseram: “Que o Abençoado venha quando lhe for conveniente.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o
Abençoado se vestiu e tomando a sua tigela e o manto externo, foi com a Sangha
dos bhikkhus até o salão de assembléias. Ao chegar, ele lavou os pés e depois
entrou no salão e sentou próximo à coluna central olhando para o leste. E os
bhikkhus lavaram os pés e depois entraram no salão e sentaram na parede do lado
oeste olhando para o leste, com o Abençoado à sua frente. E os Sakyas de
Kapilavatthu lavaram os pés e entraram no salão e sentaram na parede do lado
leste olhando para o oeste, com o Abençoado à sua frente.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o
Abençoado instruiu, motivou, estimulou e encorajou os Sakyas de Kapilavatthu
com um discurso do Dhamma, durante a maior parte da noite, depois do que ele os
dispensou dizendo: “A noite terminou, Gotamas. [<a href="#N2">2</a>] <a
name=R2></a>Vocês agora podem ir, quando lhes for conveniente.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
venerável senhor,” eles responderam. Então eles levantaram dos seus assentos e
depois de homenagear o Abençoado, mantendo-o à sua direita, partiram. Então, não
muito tempo depois que os Sakyas de Kapilavatthu haviam partido, o Abençoado se
dirigiu ao Venerável Mahamoggallana do seguinte modo: “A Sangha dos bhikkhus
está livre da preguiça e do torpor, Moggallana. Faça um discurso do Dhamma para
os bhikkhus. As minhas costas estão me incomodando. Eu irei descansá-las.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
venerável senhor,” o venerável Mahamoggallana respondeu.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, o
Abençoado dobrou em quatro o seu manto feito de retalhos e deitou no seu lado direito,
na postura do leão, com um pé sobre o outro, atento e plenamente consciente,
após anotar na mente o horário para levantar. Em seguida o Venerável
Mahamoggallana se dirigiu aos bhikkhus do seguinte modo: “Amigos, bhikkhus!”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigo!”
Aqueles bhikkhus responderam. O Venerável Mahamoggallana disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu
ensinarei para vocês, amigos, um discurso sobre o corrompido e o incorrompido.
Ouçam e prestem muita atenção àquilo que eu vou dizer.” – “Sim, venerável
senhor,” os bhikkhus responderam. O Venerável Mahamoggallana disse o seguinte::<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Como,
amigos, alguém é corrompido? Aqui, ao ver uma forma com o olho, ele se empenha
pela forma prazerosa e repele a forma des-prazerosa. Ele permanece sem
estabelecer a atenção plena no corpo, com a mente limitada, e não compreende
como na verdade é a libertação da mente, a libertação através da sabedoria, na qual
aqueles estados ruins e prejudiciais cessam sem deixar vestígio. Ao ouvir um
som com o ouvido ... Ao conscientizar um objeto mental com a mente, ele se empenha
pelo objeto mental prazeroso e repele o objeto mental des-prazeroso. Ele permanece
sem estabelecer a atenção plena no corpo, com a mente limitada, e não
compreende como na verdade é a libertação da mente, a libertação através da
sabedoria, na qual aqueles estados ruins e prejudiciais cessam sem deixar
vestígio<span style='mso-bidi-font-weight:bold'>. [<a href="#N3">3</a>]</span><o:p></o:p></span></p>



<p class=Normal><a name=R3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Isso é chamado, amigos, um bhikkhu que é corrompido no meio de formas
percebidas pelo olho, corrompido no meio de sons percebidos pelo ouvido,
corrompido no meio de aromas percebidos pelo nariz, corrompido no meio de
sabores percebidos pela língua, corrompido no meio de tangíveis percebidos pelo
corpo, corrompido no meio de objetos mentais percebidos pela mente. Quando um
bhikkhu assim permanece, se Mara se aproximar dele através do olho, Mara terá
acesso a ele, Mara tomará posse dele. Se Mara se aproximar dele através do
ouvido ... através da mente, Mara terá acesso a ele, Mara tomará posse dele.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Suponham,
amigos, que houvesse uma cabana feita de junco ou de palha, seca, dessecada,
envelhecida. Se um homem viesse do leste com uma tocha de capim em chamas, ou
do oeste, ou do norte, ou do sul, ou por baixo, ou por cima, de qualquer modo
que ele se aproximasse o fogo teria acesso à cabana, o fogo tomaria posse da
cabana. Da mesma forma, amigos, quando um bhikkhu assim permanece, se Mara se
aproximar dele através do olho ... através da mente, Mara terá acesso a ele,
Mara tomará posse dele.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando um
bhikkhu assim permanece, as formas o subjugam; ele não subjuga as formas. Os
sons o subjugam; ele não subjuga os sons. Os aromas o subjugam; ele não subjuga
os aromas. Os sabores o subjugam; ele não subjuga os sabores. Os tangíveis o
subjugam; ele não subjuga os tangíveis. Os objetos mentais o subjugam; ele não
subjuga os objetos mentais. Isso é chamado, amigos, um bhikkhu que é subjugado
pelas formas, subjugado pelos sons, subjugado pelos aromas, subjugado pelos
sabores, subjugado pelos tangíveis, subjugado pelos objetos mentais – alguém
que está subjugado e que não subjuga. Estados ruins e prejudiciais o subjugam,
estados que contaminam, que conduzem a uma renovada existência, que trazem
problemas, que resultam em sofrimento e que conduzem a um futuro nascimento,
envelhecimento e morte. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“É dessa
forma, amigos, que alguém é corrompido.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como,
amigos, alguém é incorrompido? Aqui, ao ver uma forma com o olho, ele não se
empenha pela forma prazerosa e não repele a forma des-prazerosa. Ele permanece
com a atenção plena no corpo estabelecida, com a mente ilimitada, e compreende
como na verdade é a libertação da mente, a libertação através da sabedoria, na qual
aqueles estados ruins e prejudiciais cessam sem deixar vestígio. Ao ouvir um
som com o ouvido ... Ao conscientizar um objeto mental com a mente, ele não se
empenha pelo objeto mental prazeroso e não<span style="mso-spacerun: yes"> 
</span>repele o objeto mental des-prazeroso. Ele permanece com a atenção plena
no corpo estabelecida, com a mente ilimitada, e compreende como na verdade é a
libertação da mente, a libertação através da sabedoria, na qual aqueles estados ruins
e prejudiciais cessam sem deixar vestígio.<span style='mso-bidi-font-weight:
bold'><o:p></o:p></span></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Isso é
chamado, amigos, um bhikkhu que é incorrompido no meio de formas percebidas
pelo olho, incorrompido no meio de sons percebidos pelo ouvido, incorrompido no
meio de aromas percebidos pelo nariz, incorrompido no meio de sabores
percebidos pela língua, incorrompido no meio de tangíveis percebidos pelo
corpo, incorrompido no meio de objetos mentais percebidos pela mente. Quando um
bhikkhu assim permanece, se Mara se aproximar dele através do olho, Mara irá
falhar em ter acesso a ele, Mara irá falhar em tomar posse dele. Se Mara se
aproximar dele através do ouvido ... através da mente, Mara irá falhar em ter
acesso a ele, Mara irá falhar em tomar posse dele.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Suponham,
amigos, que houvesse uma casa ou salão com uma cumeeira no telhado, construído
com argila compacta e com as paredes recém revestidas. Se um homem viesse do leste
com uma tocha de capim em chamas, ou do oeste, ou do norte, ou do sul, ou de
abaixo, ou de acima, de qualquer modo que ele se aproximasse o fogo falharia em
ter acesso à casa ou salão, o fogo falharia em tomar posse da casa ou salão. Da mesma forma,
amigos, quando um bhikkhu assim permanece, se Mara se aproximar dele através do
olho ... através da mente, Mara irá falhar em ter acesso a ele, Mara irá falhar
em tomar posse dele.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando um
bhikkhu assim permanece, ele subjuga as formas; as formas não o subjugam. Ele
subjuga os sons; os sons não o subjugam. Ele subjuga os aromas; os aromas não o
subjugam. Ele subjuga os sabores; os sabores não o subjugam. Ele subjuga os
tangíveis; os tangíveis não o subjugam. Ele subjuga os objetos mentais; os
objetos mentais não o subjugam. Isso é chamado, amigos, um bhikkhu que subjuga
as formas, subjuga os sons, subjuga os aromas, subjuga os sabores, subjuga os
tangíveis, subjuga os objetos mentais – alguém que subjuga e que não é
subjugado. Ele subjugou aqueles estados ruins e prejudiciais que contaminam,
que conduzem a uma renovada existência, que trazem problemas, que resultam em
sofrimento e que conduzem a um futuro nascimento, envelhecimento e morte.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“É dessa
forma, amigos, que alguém é incorrompido.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o
Abençoado se levantou e se dirigiu ao Venerável Mahamoggallana do seguinte
modo: “Muito bem, Moggallana! Você discursou bem para os bhikkhus o discurso
sobre o corrompido e o incorrompido.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o
que disse o<span style="mso-spacerun: yes">  </span>Venerável Mahamoggallana. O
Mestre aprovou. Os bhikkhus ficaram satisfeitos e contentes com as palavras do
Venerável Mahamoggallana.<b style='mso-bidi-font-weight:normal'><o:p></o:p></b></span></p>


