<p class=Normal style='margin-left:.75in'> 

<p class=Normal>Assim ouvi. Em certa ocasião, o Abençoado estava em Vesali na Grande Floresta no Salão com um pico na cumeeira. Então, ao amanhecer, o Abençoado se vestiu e tomando a tigela e o manto externo, foi para Vesali para esmolar alimentos.</p> 



<p class=Normal>Depois de haver esmolado em Vesali e retornado, após a refeição ele se dirigiu ao Venerável Ananda da seguinte forma: “Leve com você o seu pano para sentar, Ananda; vamos até o Santuário de Capala para passar o dia.”</p>



<p class=Normal>“Sim, venerável senhor,” o venerável Ananda respondeu, e tomando o seu pano para sentar seguiu de perto atrás do Abençoado. O Abençoado foi até o Santuário de Capala e sentou num assento que havia sido preparado.</p> 



<p class=Normal>O venerável Ananda depois de homenagear o Abençoado sentou a um lado. O Abençoado então disse para o venerável Ananda: “Encantadora é Vesali, Ananda. Encantador é o santuário Udena, encantador é o santuário Gotamaka, encantador é o santuário Sattamba, encantador é o santuário Bahuputta, encantador é o santuário Sarandada, encantador é o santuário Capala. Qualquer um, Ananda, que tenha desenvolvido e cultivado as quatro bases do poder espiritual, fez delas o seu veículo, a sua base, estabilizou-as, se exercitou nelas e aperfeiçoou-as, poderia, se ele assim desejasse, seguir vivendo uma longa vida, um éon, ou até um pouco mais. O Tathagata, Ananda, desenvolveu e cultivou as quatro bases do poder espiritual, fez delas o seu veículo, a sua base, estabilizou-as, dedicou-se a elas e aperfeiçoou-as. Se ele assim desejasse, o Tathagata poderia seguir vivendo uma longa vida ou até um pouco mais.”</p>



<p class=Normal>Mas, embora um sinal tão óbvio tenha sido dado ao Venerável Ananda pelo Abençoado, embora uma insinuação tão óbvia lhe tenha sido feita, ele foi incapaz de compreendê-la. Ele não implorou ao Abençoado: “Venerável senhor, que o Abençoado siga vivendo uma longa vida! Que o Iluminado siga vivendo durante esta longa vida, para o bem estar de muitos, para a felicidade de muitos, por compaixão pelo mundo, pelo bem, bem estar e felicidade de devas e humanos.” Até esse ponto a mente dele estava obcecada por Mara.</p>



<p class=Normal>Uma segunda vez, o Abençoado disse para o venerável Ananda: “Encantadora é Vesali, Ananda. Encantador é o santuário Udena, encantador é o santuário Gotamaka, encantador é o santuário Sattamba, encantador é o santuário Bahuputta, encantador é o santuário Sarandada, encantador é o santuário Capala. Qualquer um, Ananda, que tenha desenvolvido e cultivado as quatro bases do poder espiritual, fez delas o seu veículo, a sua base, estabilizou-as, se exercitou nelas e aperfeiçoou-as, poderia, se ele assim desejasse, seguir vivendo uma longa vida ou até um pouco mais. O Tathagata, Ananda, desenvolveu e cultivou as quatro bases do poder espiritual, fez delas o seu veículo, a sua base, estabilizou-as, dedicou-se a elas e aperfeiçoou-as. Se ele assim desejasse, o Tathagata poderia seguir vivendo uma longa vida ou até um pouco mais.”</p>



<p class=Normal>Mas, embora um sinal tão óbvio tenha sido dado ao Venerável Ananda pelo Abençoado, embora uma insinuação tão óbvia lhe tenha sido feita, ele foi incapaz de compreendê-la. Ele não implorou ao Abençoado: “Venerável senhor, que o Abençoado siga vivendo uma longa vida! Que o Iluminado siga vivendo durante esta longa vida, para o bem estar de muitos, para a felicidade de muitos, por compaixão pelo mundo, pelo bem, bem estar e felicidade de devas e humanos.” Até esse ponto a mente dele estava obcecada por Mara.</p>



<p class=Normal>Uma terceira vez, o Abençoado disse para o venerável Ananda: “Encantadora é Vesali, Ananda. Encantador é o santuário Udena, encantador é o santuário Gotamaka, encantador é o santuário Sattamba, encantador é o santuário Bahuputta, encantador é o santuário Sarandada, encantador é o santuário Capala. Qualquer um, Ananda, que tenha desenvolvido e cultivado as quatro bases do poder espiritual, fez delas o seu veículo, a sua base, estabilizou-as, se exercitou nelas e aperfeiçoou-as, poderia, se ele assim desejasse, seguir vivendo uma longa vida ou até um pouco mais. O Tathagata, Ananda, desenvolveu e cultivou as quatro bases do poder espiritual, fez delas o seu veículo, a sua base, estabilizou-as, dedicou-se a elas e aperfeiçoou-as. Se ele assim desejasse, o Tathagata poderia seguir vivendo uma longa vida ou até um pouco mais.”</p>



<p class=Normal>Mas, embora um sinal tão óbvio tenha sido dado ao Venerável Ananda pelo Abençoado, embora uma insinuação tão óbvia lhe tenha sido feita, ele foi incapaz de compreendê-la. Ele não implorou ao Abençoado: “Venerável senhor, que o Abençoado siga vivendo uma longa vida! Que o Iluminado siga vivendo durante esta longa vida, para o bem estar de muitos, para a felicidade de muitos, por compaixão pelo mundo, pelo bem, bem estar e felicidade de devas e humanos.” Até esse ponto a mente dele estava obcecada por Mara.</p>



<p class=Normal>Então, o Abençoado se dirigiu ao Venerável Ananda: “Você pode ir, Ananda, quando lhe for conveniente.”</p>



<p class=Normal>“Sim, venerável senhor,” o Venerável Ananda respondeu, levantou-se do seu assento, homenageou o Abençoado e mantendo-o à sua direita, sentou ao pé de uma árvore ao lado.</p>



<p class=Normal>Então, pouco tempo depois que o venerável Ananda havia partido, Mara, o Senhor do Mal, foi até o Abençoado e disse:</p>



<p class=Normal>”Venerável senhor, que o Abençoado agora realize o parinibbana! Que o Iluminado agora realize o parinibbana! Agora é o momento para o parinibbana do Abençoado! Esta afirmação, venerável senhor, foi feita pelo Abençoado: ‘Eu não realizarei o parinibbana , Senhor do Mal, até que eu tenha discípulos bhikkhus que sejam sábios, disciplinados, confiantes, libertados do cativeiro, estudados, defensores do Dhamma, praticando de acordo com o Dhamma, praticando do modo correto, comportando-se de modo apropriado; que tenham aprendido a doutrina do mestre e sejam capazes de explicá-la, ensiná-la, proclamá-la, estabelecê-la, revelá-la, analisá-la e elucidá-la; que sejam capazes de refutar completamente com argumentos as doutrinas dos outros e que sejam capazes de ensinar o Dhamma que é eficaz.’ Mas no momento, venerável senhor, o Abençoado tem discípulos bhikkhus que são sábios ... que são capazes de ensinar o Dhamma que é eficaz.</p> 



<p class=Normal>”Venerável senhor, que o Abençoado agora realize o parinibbana! Que o Iluminado agora realize o parinibbana! Agora é o momento para o parinibbana do Abençoado!</p>



<p class=Normal>“E esta afirmação, venerável senhor, foi feita pelo Abençoado: ‘Eu não realizarei o parinibbana, Senhor do Mal, até que eu tenha discípulas bhikkhunis ... até que eu tenha discípulos leigos ... até que eu tenha discípulas leigas que sejam sábias ... e que sejam capazes de ensinar o Dhamma que é eficaz.’ </p>



<p class=Normal>Mas no momento, venerável senhor, o Abençoado tem discípulas leigas que são sábias, disciplinadas, confiantes, libertadas do cativeiro, estudadas, defensoras do Dhamma, praticando de acordo com o Dhamma, praticando do modo correto, comportando-se de modo apropriado; que aprenderam a doutrina do mestre e que são capazes de explicá-la, ensiná-la, proclamá-la, estabelecê-la, revelá-la, analisá-la e elucidá-la; que são capazes de refutar completamente com argumentos as doutrinas dos outros e que são capazes de ensinar o Dhamma que é eficaz.</p> 



<p class=Normal>Venerável senhor, que o Abençoado agora realize o parinibbana! Que o Iluminado agora realize o parinibbana! Agora é o momento para o parinibbana do Abençoado!</p>



<p class=Normal>“E esta afirmação, venerável senhor, foi feita pelo Abençoado: ‘Eu não realizarei o parinibbana, Senhor do Mal, até que esta minha vida santa tenha se tornado bem sucedida e próspera, extensa, popular, expandida, bem proclamada entre os devas e humanos.’ A vida santa do Abençoado, venerável senhor, se tornou bem sucedida e próspera, extensa, popular, expandida, bem proclamada entre os devas e humanos.</p>



<p class=Normal>Venerável senhor, que o Abençoado agora realize o parinibbana! Que o Iluminado agora realize o parinibbana! Agora é o momento para o parinibbana do Abençoado!”</p>



<p class=Normal>Quando isso foi dito, o Abençoado disse para Mara o Senhor do Mal: “Fique tranqüilo, Senhor do Mal. Não tardará muito até que ocorra o parinibbana do Tathagata. Daqui a três meses o Tathagata realizará o parinibbana.”</p>



<p class=Normal>Então, o Abençoado, no santuário de Capala, com atenção plena e plena consciência renunciou à sua formação vital. E quando o Abençoado renunciou à sua formação vital, ocorreu um grande terremoto, assustador e aterrorizador e o estrondo de trovões sacudiram o céu.</p>



<p class=Normal>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou: </p>



	<p class=Normal style='margin-left:.75in'> “Comparando o incomparável com a existência continuada, <br>

	o sábio renunciou à formação do ser/existir.<br>

	Regozijando-se no íntimo, concentrado, ele rompeu<br>

	a continuada existência tal qual uma armadura.”</p>

