<p class=Normal> Em Savatthi. “Bhikkhus, aqueles contemplativos ou brâmanes que não compreendem essas coisas, a origem dessas coisas, a cessação dessas coisas, e o caminho que conduz à cessação dessas coisas: quais são essas coisas que eles não compreendem, cuja origem eles não compreendem, cuja cessação eles não compreendem, e cujo caminho que conduz à cessação eles não compreendem?</p>

<p class=Normal>"Eles não compreendem o envelhecimento e morte, a sua origem, a sua cessação e o caminho que conduz à sua cessação. Eles  não compreendem o nascimento ... ser/existir ... apego ... desejo ... sensação ... contato ... seis bases dos sentidos .... mentalidade-materialidade (nome e forma) ... consciência ... formações volitivas, a sua origem, a sua cessação e o caminho que conduz à sua cessação. Essas são as  coisas que eles não compreendem, cuja origem eles não compreendem, cuja cessação eles não compreendem, e cujo caminho que conduz à cessação eles não compreendem. </p>

<p class=Normal>"Esses eu não considero contemplativos dentre contemplativos ou brâmanes dentre brâmanes, e esses veneráveis não irão, realizando por si mesmos através do conhecimento direto, nesta mesma vida, entrar e permanecer no objetivo da vida contemplativa ou no objetivo da vida brâmane.</p>

<p class=Normal> “Mas, bhikkhus, aqueles contemplativos ou brâmanes que compreendem essas coisas, a origem dessas coisas, a cessação dessas coisas, e o caminho que conduz à cessação dessas coisas: quais são essas coisas que eles compreendem, cuja origem eles compreendem, cuja cessação eles compreendem, e cujo caminho que conduz à cessação eles compreendem?</p>

<p class=Normal>"Eles compreendem o envelhecimento e morte, a sua origem, a sua cessação e o caminho que conduz à sua cessação. Eles compreendem o nascimento ... ser/existir ... apego ... desejo ... sensação ... contato ... seis bases dos sentidos .... mentalidade-materialidade (nome e forma) ... consciência ... formações volitivas, a sua origem, a sua cessação e o caminho que conduz à sua cessação. Essas são as  coisas que eles compreendem, cuja origem eles compreendem, cuja cessação eles compreendem, e cujo caminho que conduz à cessação eles compreendem. </p>

<p class=Normal>"Esses eu considero contemplativos dentre contemplativos ou brâmanes dentre brâmanes, e esses veneráveis irão, realizando por si mesmos através do conhecimento direto, nesta mesma vida, entrar e permanecer no objetivo da vida contemplativa ou no objetivo da vida brâmane.</p>

