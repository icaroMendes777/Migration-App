<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Certa
ocasião, o Abençoado estava entre os Kosalas numa pequena cabana na floresta na
região do Himalaia. Então, enquanto o Abençoado estava só e isolado, um pensamento
surgiu na sua mente: “É possível exercer o governo com virtude: sem matar e sem
instigar os outros a que matem, sem confiscar e sem instigar os outros a que
confisquem, sem sofrer e sem causar o sofrimento?”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então,
Mara, o Senhor do Mal, tomando conhecimento com a sua mente do pensamento na
mente do Abençoado foi até o Abençoado e disse: “Venerável senhor, que o
Abençoado governe com virtude: sem matar e sem instigar os outros a que matem,
sem confiscar e sem instigar os outros a que confisquem, sem sofrer e sem
causar o sofrimento.”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas o que
você vê, Senhor do Mal, para falar comigo dessa forma?”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, o Abençoado desenvolveu e cultivou as quatro bases do poder espiritual,
fez delas o seu veículo, a sua base, estabilizou-as, se exercitou nelas e
aperfeiçoou-as. E, venerável senhor, se o Abençoado desejar, ele só tem que
decidir que o Himalaia, o rei dentre as montanhas, se transforme em ouro, e ele
se transformará em ouro.” [<a href="#N1">1</a>]<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><a name=R1></a><span lang=PT-BR
