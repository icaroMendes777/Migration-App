<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] Dizem que aquele que obtém maestria sobre as quatro bases do poder
espiritual poderia viver por tanto tempo quanto um éon ou aquilo que resta de
um éon. Mara fez esse apelo ao Buda, não devido ao respeito pela sua habilidade
como líder, mas por querer tentá-lo com a cobiça pelo poder e dessa forma
controlá-lo. É interessante que este sutta não proporciona uma resposta à
questão sobre a possibilidade de um governo virtuoso, e essa ambigüidade está
presente em todo o Cânone em Pali. Enquanto alguns textos admitem que pode
surgir um “monarca que gire a roda”, (um monarca justo que governa de acordo com o Dhamma), o consenso geral é que o exercício da
autoridade envolve algum tipo de violência e assim se torna difícil a sua
compatibilidade com a perfeita obediência aos preceitos. [<a href="#R1">Retorna</a>]<o:p></o:p></span></p>


