<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim ouvi. [<a href="#NA">A</a>]
<a name=RA></a>
Em certa ocasião, o Abençoado estava em Vesali na Grande Floresta no Salão com
um pico na cumeeira. Então, ao amanhecer, o Abençoado se vestiu e tomando a
tigela e o manto externo, foi para Vesali para esmolar alimentos. Depois de
haver esmolado em Vesali e retornado, após a refeição ele se dirigiu ao
Venerável Ananda da seguinte forma:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Leve com
você o seu pano para sentar, Ananda; vamos até o Santuário de Capala para
passar o dia.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
venerável senhor,” o venerável Ananda respondeu, e tomando o seu pano para
sentar seguiu de perto atrás do Abençoado. O Abençoado foi até o Santuário de
Capala e sentou num assento que havia sido preparado. O venerável Ananda depois
de homenagear o Abençoado sentou a um lado. O Abençoado então disse para o
venerável Ananda:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Encantadora
é Vesali, Ananda. Encantador é o santuário Udena, encantador é o santuário
Gotamaka, encantador é o santuário Sattamba, encantador é o santuário
Bahuputta, encantador é o santuário Sarandada, encantador é o santuário Capala.
Qualquer um, Ananda, que tenha desenvolvido e cultivado as quatro bases do
poder espiritual, fez delas o seu veículo, a sua base, estabilizou-as, se
exercitou nelas e aperfeiçoou-as, poderia, se ele assim desejasse, seguir
vivendo durante aquele éon ou o tempo que restasse de um éon. [<a href="#N1">1</a>]
<a name=R1></a>O Tathagata, Ananda, desenvolveu e cultivou as quatro bases do
poder espiritual, fez delas o seu veículo, a sua base, estabilizou-as, se
exercitou nelas e aperfeiçoou-as. Se ele assim desejasse, o Tathagata poderia
seguir vivendo durante este éon ou o tempo que resta do éon.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Mas, embora
um sinal tão óbvio tenha sido dado ao Venerável Ananda pelo Abençoado, embora
uma insinuação tão óbvia lhe tenha sido feita, ele foi incapaz de
compreendê-la. Ele não implorou ao Abençoado: “Venerável senhor, que o
Abençoado siga vivendo durante este éon! Que o Iluminado siga vivendo durante
este éon, para o bem estar de muitos, para a felicidade de muitos, por
compaixão pelo mundo, pelo bem, bem estar e felicidade de devas e humanos.” Até
esse ponto a mente dele estava obcecada por Mara. [<a href="#N2">2</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Uma segunda vez ... Uma terceira vez o Abençoado se dirigiu ao Venerável
Ananda: “Encantadora é Vesali, Ananda ... Qualquer um, Ananda, que tenha
desenvolvido e cultivado as quatro bases do poder espiritual ... poderia se ele
assim desejasse, seguir vivendo durante aquele éon ou o tempo que restar de um
éon ... Se ele assim desejasse, o Tathagata poderia seguir vivendo durante este
éon ou o tempo que resta do éon.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Mas
novamente, embora um sinal tão óbvio tenha sido dado ao Venerável Ananda pelo
Abençoado, embora uma insinuação tão óbvia lhe tenha sido feita, ele foi
incapaz de compreendê-la ... Até esse ponto a mente dele estava obcecada por
Mara.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, o
Abençoado se dirigiu ao Venerável Ananda: “Você pode ir, Ananda, quando lhe for
conveniente.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
venerável senhor,” o Venerável Ananda respondeu, levantou-se do seu assento,
homenageou o Abençoado e mantendo-o à sua direita, sentou ao pé de uma árvore
ao lado. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então,
pouco tempo depois que o venerável Ananda havia partido, Mara, o Senhor do Mal,
foi até o Abençoado e disse: <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, que o Abençoado agora realize o <span class=GramE>parinibbana !</span>
Que o Iluminado agora realize o parinibbana ! Agora é o momento para o
parinibbana do Abençoado! Esta afirmação, venerável senhor, foi feita pelo
Abençoado: ‘Eu não realizarei o parinibbana , Senhor do Mal, até
que eu tenha discípulos bhikkhus que sejam sábios, disciplinados, confiantes,
libertados do cativeiro, estudados, defensores do Dhamma, praticando de acordo
com o Dhamma, praticando do modo correto, comportando-se de modo apropriado;
que tenham aprendido a doutrina do mestre e sejam capazes de explicá-la,
ensiná-la, proclamá-la, estabelecê-la, revelá-la, analisá-la e elucidá-la; que
sejam capazes de refutar completamente com argumentos as doutrinas dos outros e
que sejam capazes de ensinar o Dhamma que é eficaz.’ Mas no momento, venerável
senhor, o Abençoado tem discípulos bhikkhus que são sábios ... que são capazes
de ensinar o Dhamma que é eficaz. Venerável senhor, que o Abençoado agora
realize o parinibbana ! Que o Iluminado agora realize o parinibbana ! Agora é o
momento para o parinibbana do Abençoado!<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E esta
afirmação, venerável senhor, foi feita pelo Abençoado: ‘Eu não realizarei o parinibbana,
Senhor do Mal, até que eu tenha discípulos <span class=GramE>bhikkhuni ...</span>
<span style='mso-spacerun:yes'> </span>até que eu tenha discípulos leigos
homens ... até que eu tenha discípulos leigos mulheres que sejam sábias ... e
que sejam capazes de ensinar o Dhamma que é eficaz.’ Mas no momento, venerável
senhor, o Abençoado tem discípulos leigos mulheres que são sábias,
disciplinadas, confiantes, libertadas do cativeiro, estudadas, defensoras do
Dhamma, praticando de acordo com o Dhamma, praticando do modo correto,
comportando-se de modo apropriado; que aprenderam a doutrina do mestre e que
são capazes de explicá-la, ensiná-la, proclamá-la, estabelecê-la, revelá-la,
analisá-la e elucidá-la; que são capazes de refutar completamente com
argumentos as doutrinas dos outros e que são capazes de ensinar o Dhamma que é
eficaz. Venerável senhor, que o Abençoado agora realize o parinibbana ! Que o
Iluminado agora realize o parinibbana ! Agora é o momento para o parinibbana do
Abençoado!<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E esta
afirmação, venerável senhor, foi feita pelo Abençoado: ‘Eu não realizarei o parinibbana,
Senhor do Mal, até que esta minha vida santa tenha se tornado bem sucedida e
próspera, extensa, popular, expandida, bem proclamada entre os devas e <span
class=GramE>humanos.</span>’ A vida santa do Abençoado, venerável senhor, se
tornou bem sucedida e próspera, extensa, popular, expandida, bem proclamada
entre os devas e humanos.[<a href="#N3">3</a>] <a name=R3></a>Venerável senhor,
que o Abençoado agora realize o parinibbana ! Que o Iluminado agora realize o
parinibbana ! Agora é o momento para o parinibbana do Abençoado!”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isso
foi dito, o Abençoado disse para Mara o Senhor do Mal: “Fique tranqüilo, Senhor
do Mal. Não tardará muito até que ocorra o parinibbana do Tathagata. Daqui a
três meses o Tathagata realizará o parinibbana .”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, o
Abençoado, no santuário de Capala, com atenção plena e plena consciência
renunciou à sua formação vital. [<a href="#N4">4</a>] <a name=R4></a>E quando o
Abençoado renunciou à sua formação vital, ocorreu um grande terremoto,
assustador e aterrorizador e o estrondo de trovões sacudiram o céu. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então,
tendo compreendido o significado daquilo, o Abençoado naquela ocasião recitou
estes versos inspirados:<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Comparando o incomparável com a existência continuada, <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>o sábio renunciou à formação do ser/existir.<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Regozijando-se no íntimo, concentrado, ele rompeu<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>a continuada existência tal qual uma armadura.”<o:p></o:p></span></p>



