<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’Eu vejo
alguém que é puro, nobre e saudável; a purificação de uma pessoa surge daquilo
que ela vê’ – assim, tendo essa opinião e acreditando que essa idéia é a
melhor, ele considera que o conhecimento consiste em ver alguém que é puro.[<a
href="#N1">1</a>]<span style='mso-spacerun:yes'>  </span><o:p></o:p></span></p>


<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Mas se através daquilo que é visto alguém pudesse ser purificado ou por
meio desse conhecimento o sofrimento ser abandonado, então, aquele que ainda
tem apegos poderia ser purificado por outro. No entanto, essa é a mera opinião
daqueles que assim afirmam. <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Nenhum
brâmane [<a href="#N2">2</a>] <a name=R2></a>diz que alguém pode ser purificado
por outro, nem por aquilo que é visto, ouvido ou sentido, tampouco através de
preceitos e práticas. Ele (o verdadeiro brâmane) não é maculado pelo mérito ou
pelo demérito, ele não mais se envolve na geração de kamma e abandona aquilo
que havia abraçado, ele abandona o eu. <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Aqueles que
abandonam uma coisa para se agarrar a outra, [<a href="#N3">3</a>] <a name=R3></a>dominados
pelo desejo eles não superam o apego. Eles soltam e agarram, igual a um macaco
que solta de um galho para se agarrar a outro.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Uma pessoa,
tendo adotado certas práticas, aprisionada aos sentidos, passa de estados
superiores aos inferiores. Mas aquele que é sábio, tendo buscado e compreendido
o Dhamma através do conhecimento direto, não passa dos estados superiores aos
inferiores. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ele está
livre de inimigos [<a href="#N4">4</a>] <a name=R4></a>com relação a todas as
coisas vistas, ouvidas ou sentidas. Por quem, como, poderia ele ser
classificado no mundo? Alguém que viu com clareza, como as coisas na verdade são,
e que tem a conduta aberta? [<a href="#N5">5</a>] <o:p></o:p></span></p>


<p class=Normal><a name=R5></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Eles nem formulam e tampouco propõem teorias. Eles não dizem<span
style='mso-spacerun:yes'>  </span>- ‘esta é a pureza suprema.’ Desatando o nó
do apego, eles não criam desejo por nada em absoluto no mundo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O brâmane
que foi além das limitações, [<a href="#N6">6</a>] <a name=R6></a>não tem apego
pelo conhecimento ou visão. Ele não está apegado ao apego nem está apegado ao
desapego. [<a href="#N7">7</a>] <a name=R7></a>Neste mundo não há nada que ele
tenha agarrado como supremo. <o:p></o:p></span></p>

