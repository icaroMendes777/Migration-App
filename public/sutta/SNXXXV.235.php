<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
eu ensinarei para vocês um discurso do Dhamma sobre o fogo. Ouçam e prestem muita
atenção àquilo que eu vou dizer.” – “Sim, venerável senhor,” os bhikkhus
responderam. O Abençoado disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é o discurso do Dhamma sobre o fogo? Seria melhor, bhikkhus, a
faculdade do olho ser lacerada com um prego de ferro incandescente, inflamado e
brilhante, do que uma pessoa se apegar aos sinais ou detalhes de uma forma
percebida pelo olho. Pois, se a consciência estiver atada à gratificação dos
sinais ou dos detalhes, e se naquela ocasião ela morrer, é possível que ela
tenha um destes dois destinos: o inferno ou o reino animal. Ao ver esse perigo,
eu digo isso. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Seria
melhor, bhikkhus, a faculdade do ouvido ser lacerada com uma haste de ferro
incandescente, inflamada e brilhante, do que uma pessoa se apegar aos sinais ou
detalhes de um som percebido pelo ouvido. Pois, se a consciência estiver atada
à gratificação dos sinais ou dos detalhes, e se naquela ocasião ela morrer, é
possível que ela tenha um destes dois destinos: o inferno ou o reino animal. Ao
ver esse perigo, eu digo isso.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Seria
melhor, bhikkhus, a faculdade do nariz ser lacerada com uma tesoura afiada
incandescente, inflamada e brilhante, do que uma pessoa se apegar aos sinais ou
detalhes de um aroma percebido pelo nariz. Pois, se a consciência estiver atada
à gratificação dos sinais ou dos detalhes, e se naquela ocasião ela morrer, é
possível que ela tenha um destes dois destinos: o inferno ou o reino animal. Ao
ver esse perigo, eu digo isso.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Seria
melhor, bhikkhus, a faculdade da língua ser lacerada com uma navalha afiada
incandescente, inflamada e brilhante, do que uma pessoa se apegar aos sinais ou
detalhes de um sabor percebido pela língua. Pois se a consciência estiver atada
à gratificação dos sinais ou dos detalhes, e se naquela ocasião ela morrer, é
possível que ela tenha um destes dois destinos: o inferno ou o reino animal. Ao
ver esse perigo, eu digo isso.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Seria
melhor, bhikkhus, a faculdade do corpo ser lacerada com uma lança afiada
incandescente, inflamada e brilhante, do que uma pessoa se apegar aos sinais ou
detalhes de um tangível percebido pelo corpo. Pois se a consciência estiver
atada à gratificação dos sinais ou dos detalhes, e se naquela ocasião ela
morrer, é possível que ela tenha um destes dois destinos: o inferno ou o reino
animal. Ao ver esse perigo, eu digo isso.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Seria
melhor, bhikkhus, dormir, pois o sono, eu digo, é estéril para os seres vivos,
infrutífero para os seres vivos, insensitivo para os seres vivos, do que pensar
pensamentos que poderiam induzir uma pessoa a cometer um cisma na Sangha. Ao
ver esse perigo, eu digo isso.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Com
respeito a isso, bhikkhus, o nobre discípulo bem instruído reflete da seguinte
forma: ‘Deixemos de lado a laceração da faculdade do olho com um prego de ferro
incandescente, inflamado e brilhante. Que eu dê atenção apenas a isto: então, o
olho é impermanente, as formas são impermanentes, a consciência no olho é
impermanente, o contato no olho é impermanente, qualquer sensação que surja
tendo o contato no olho como condição, quer seja prazerosa, dolorosa, ou nem
prazerosa, nem dolorosa, isso também é impermanente.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Deixemos
de lado a laceração da faculdade do ouvido com uma haste de ferro
incandescente, inflamada e brilhante. Que eu dê atenção apenas a isto: então, o
ouvido é impermanente, os sons são impermanentes, a consciência no ouvido é
impermanente, o contato no ouvido é impermanente, qualquer sensação que surja
tendo o contato no ouvido como condição ... isso também é impermanente.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Deixemos
de lado a laceração da faculdade do nariz com uma tesoura afiada incandescente,
inflamada e brilhante. Que eu dê atenção apenas a isto: então, o nariz é
impermanente, os aromas são impermanentes, a consciência no nariz é
impermanente, o contato no nariz é impermanente, qualquer sensação que surja
tendo o contato no nariz como condição ... isso também é impermanente.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Deixemos
de lado a laceração da faculdade da língua com uma navalha afiada
incandescente, inflamada e brilhante. Que eu dê atenção apenas a isto: então a
língua é impermanente, os sabores são impermanentes, a consciência na língua é
impermanente, o contato na língua é impermanente, qualquer sensação que surja
tendo o contato na língua como condição ... isso também é impermanente.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Deixemos
de lado a laceração da faculdade do corpo com uma lança afiada incandescente,
inflamada e brilhante. Que eu dê atenção apenas a isto: então, o corpo é
impermanente, os tangíveis são impermanentes, a consciência no corpo é
impermanente, o contato no corpo é impermanente, qualquer sensação que surja
tendo o contato no corpo como condição ... isso também é impermanente.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Deixemos
de lado o dormir. Que eu dê atenção apenas a isto: então, a mente é
impermanente, os objetos mentais são impermanentes, a consciência na mente é
impermanente, o contato na mente é impermanente, qualquer sensação que surja
tendo o contato na mente como condição ... isso também é impermanente<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Vendo
dessa forma, bhikkhus, o nobre discípulo bem instruído se desencanta com o
olho, formas, consciência no olho, contato no olho e qualquer sensação que
surja tendo o contato no olho como condição, quer seja prazerosa, dolorosa, ou
nem prazerosa, nem dolorosa ... com a mente, objetos mentais, consciência na
mente, contato na mente, qualquer sensação que surja tendo o contato na mente
como condição ... Desencantado, ele se torna desapegado. Através do desapego a
sua mente é libertada. Quando ela está libertada surge o conhecimento:
‘Libertada.’ Ele compreende que: ‘O nascimento foi destruído, a vida santa foi
vivida, o que deveria ser feito foi feito, não há mais vir a ser a nenhum
estado.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Este,
bhikkhus, é o discurso do Dhamma sobre o fogo.”<o:p></o:p></span></p>


