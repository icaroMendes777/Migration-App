<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, quando alguém permanece contemplando a gratificação nas
coisas que agrilhoam, há o estabelecimento da consciência.[<a href="#N1">1</a>]
<a name=R1></a>Com a consciência como condição, a mentalidade-materialidade (nome e forma)
[surge]. Da mentalidade-materialidade (nome e forma) como condição, as seis bases dos
sentidos. Das seis bases dos sentidos como condição, o contato. Do contato como
condição, a sensação. Da sensação como condição, o desejo. Do desejo como
condição, o apego. Do apego como condição, o ser/existir. Do ser/existir como
condição, o nascimento. Do nascimento como condição, então o envelhecimento e
morte, tristeza, lamentação, dor, angústia e desespero surgem. Essa é a origem
de toda essa massa de sofrimento.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Suponham,
bhikkhus, que houvesse uma grande árvore e todas as suas raízes espalhadas pela
terra enviassem a seiva para cima. Sustentada por essa seiva, alimentada por ela,
essa grande árvore se manteria em pé por muito tempo. Da mesma forma, quando
alguém permanece contemplando a gratificação nas coisas que agrilhoam, há o
estabelecimento da consciência ... Essa é a origem de toda essa massa de
sofrimento.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando,
bhikkhus, alguém permanece contemplando o perigo nas coisas que agrilhoam, não
há o estabelecimento da consciência. Da cessação da consciência, cessa a
mentalidade-materialidade (nome e forma). Da cessação da mentalidade-materialidade (nome e forma), cessam as
seis bases dos sentidos. Da cessação das seis bases dos sentidos, cessa o
contato. Da cessação do contato, cessa a sensação. Da cessação da sensação,
cessa o desejo. Da cessação do desejo, cessa o apego. Da cessação do apego,
cessa o ser/existir. Da cessação do ser/existir, cessa o nascimento. Da
cessação do nascimento, então o envelhecimento e morte, tristeza, lamentação,
dor, angústia e desespero, tudo cessa. Essa é a cessação de toda essa massa de
sofrimento.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Suponham,
bhikkhus, que houvesse uma grande árvore. Então um homem viesse trazendo uma pá
e um cesto. Ele cortaria a árvore pela raiz, cavaria e arrancaria todas as
raízes, até mesmo as mais delgadas. Ele cortaria a árvore em pedaços, partiria
os pedaços em pedaços menores e os reduziria a lascas. Depois ele secaria as
lascas ao vento e ao sol, queimaria essas lascas no fogo e recolheria as
cinzas. Tendo feito isso, ele deixaria as cinzas serem levadas pelo vento ou as
jogaria na correnteza de um rio. Assim, aquela grande árvore teria sido cortada
pela raiz, feita como um tronco de palmeira, eliminada de tal forma que não estará
mais sujeita a um a um futuro surgimento.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Da mesma
forma, bhikkhus, quando alguém permanece contemplando o perigo nas coisas que
agrilhoam, não há o estabelecimento da consciência ...<span
style="mso-spacerun: yes">  </span>Essa é a cessação de toda essa massa de
sofrimento.”<o:p></o:p></span></p>




