<p class=Normal style='margin-left:.75in'>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim ouvi.
Em certa ocasião, o Abençoado estava<span style='mso-spacerun:yes'>  </span>em
Savatthi no Bosque de Jeta, no Parque de Anathapindika. Agora, naquela ocasião,
Bahiya, do manto de casca de árvore, estava em Supparaka na praia. Ele era
adorado, reverenciado, venerado, homenageado – recebedor de mantos, comida
esmolada, moradia e medicamentos. Então, estando só em isolamento, o seguinte
pensamento surgiu na mente dele: “Agora, entre aqueles que neste mundo são
arahants ou que entraram no caminho para o estado de arahant, eu sou um deles?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, um
deva que havia sido um companheiro de Bahiya – compassivo, desejando o seu
bem-estar, percebendo com a sua mente o pensamento na mente dele – foi até onde
ele estava e ao chegar disse: “Você, Bahiya, não é nem um arahant e tampouco
entrou no caminho para o estado de arahant. Você nem mesmo pratica de modo a
realizar o estado de arahant ou entrar no caminho do estado de arahant.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas quem,
vivendo neste mundo com os seus devas, é um arahant ou entrou no caminho para o
estado de arahant?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bahiya, há
uma cidade na região norte chamada Savatthi. O Abençoado, um arahant,
perfeitamente iluminado – está lá agora. Ele é um arahant de verdade e ensina o
Dhamma que conduz ao estado de arahant.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então
Bahiya, tendo sido repreendido pelo deva, deixou Supparaka naquele mesmo
instante e no espaço de uma noite percorreu todo o caminho até onde o Abençoado
estava, em Savatthi no Bosque de Jeta, no Parque de Anathapindika. Naquela
ocasião, um grande número de bhikkhus estavam praticando a meditação andando ao
ar livre. Ele foi até eles e disse: “Onde, veneráveis senhores, está o
Abençoado – o arahant, perfeitamente iluminado? Nós desejamos vê-lo.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele foi
até a cidade esmolar alimentos.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então,
Bahiya saiu apressado do bosque de Jeta e foi para Savatthi, onde ele viu o
Abençoado esmolando alimentos – confiante e inspirando confiança, com os
sentidos em paz, com a mente em paz, tendo alcançado a tranqüilidade e controle
supremos, treinado, guardado, as faculdades dos sentidos controladas, um naga.
[<a href="#NA">A</a>] <a name=RA></a>Ao vê-lo ele foi até o Abençoado e ao
chegar se lançou ao solo com a cabeça aos pés do Abençoado e disse: “Ensine-me
o Dhamma, Abençoado! Ensine-me o Dhamma, Iluminado, que isso me trará
felicidade e bem-estar por muito tempo.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isso
foi dito, o Abençoado respondeu: “Este não é o momento, Bahiya. Nós entramos na
cidade para esmolar alimentos.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Uma segunda
vez, Bahiya disse para o Abençoado: “Mas é difícil saber com certeza quais são
os perigos que podem ameaçar a vida do Abençoado, ou quais perigos podem
ameaçar a minha vida. Ensine-me o Dhamma, Abençoado! Ensine-me o Dhamma,
Iluminado, que isso me trará felicidade e bem-estar por muito tempo.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Uma segunda
vez, o Abençoado respondeu: “Este não é o momento, Bahiya. Nós entramos na
cidade para esmolar alimentos.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Uma
terceira vez, Bahiya disse para o Abençoado: “Mas é difícil saber com certeza
quais são os perigos que podem ameaçar a vida do Abençoado, ou quais perigos podem
ameaçar a minha vida. Ensine-me o Dhamma, Abençoado! Ensine-me o Dhamma,
Iluminado, que isso me trará felicidade e bem-estar por muito tempo.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
Bahiya, você deve treinar assim: Com relação ao que é visto, haverá apenas o visto.
Com relação ao que é ouvido, haverá apenas o ouvido. Com relação ao que é
sentido, haverá apenas o sentido. Com relação<span style='mso-spacerun:yes'> 
</span>ao que é conscientizado, haverá apenas o conscientizado. Assim é como
você deve treinar. Quando com relação ao que é visto houver apenas o visto, ao
que é ouvido houver apenas o ouvido, ao que é sentido houver apenas o sentido,
ao que é conscientizado houver apenas o conscientizado, então, Bahiya, você não
estará ‘com aquilo.’ Quando você não estiver ‘com aquilo,’ então você não estará
‘naquilo.’ Quando você não estiver ‘naquilo,’ então você não estará aqui, nem
além e tampouco entre os dois. Isso em si mesmo é o fim do sofrimento.” [</span><a
href="#N1"><span lang=PT-BR style='mso-ansi-language:PT-BR'>1</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Ouvindo essa explicação resumida do Dhamma do Abençoado, a mente de Bahiya exatamente
naquele momento se libertou das impurezas através do desapego. Tendo exortado
Bahiya com essa explicação resumida do Dhamma, o Abençoado partiu. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Agora, não
muito tempo depois do Abençoado ter partido, Bahiya foi atacado por uma vaca
com o seu bezerro e morreu. Então o Abençoado, depois de haver esmolado em
Savatthi e ao retornar com uma grande comitiva de bhikkhus, viu que Bahiya
havia morrido. Ao vê-lo, ele disse para os bhikkhus: “Tomem o corpo de Bahiya e
o coloquem sobre uma liteira e o levem para ser cremado e depois construam um
memorial. O companheiro de vocês na vida santa morreu.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Assim
seja, venerável senhor,” os bhikkhus responderam. Depois de ter colocado o
corpo de Bahiya numa liteira eles o levaram para ser cremado e depois construíram
um memorial.<span style='mso-spacerun:yes'>  </span>Em seguida foram até o
Abençoado e depois de cumprimentá-lo sentaram a um lado e disseram: “O corpo de
Bahiya foi cremado, venerável senhor, e o memorial foi construído. Qual será o
destino dele? Qual será o futuro percurso dele?” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
Bahiya era sábio. Ele praticava o Dhamma de acordo com o Dhamma e não me
importunava com questões relativas ao Dhamma. Bahiya realizou o parinibbana.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então,
dando-se conta do significado disso, o Abençoado nessa ocasião exclamou: <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>[Onde a
terra, água, fogo e ar, grande e pequeno, fino e grosseiro, puro e impuro – não
encontram apoio?:] [<a href="#N2">2</a>]<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'>
<a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Onde as estrelas não brilham,<br>

&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;o sol não resplandece,<br>

&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;a lua não brilha,<br>

&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;a escuridão não é vista. [<a href="#N3">3</a>]<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:PT-BR'><a name=R3></a>E quando um
sábio,<br>

&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;um brâmane através da sabedoria,<br>

&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;compreendeu isso de modo direto,<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:PT-BR'>então do
material e do imaterial,<br>

&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;do prazer e da dor,<br>

&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;ele está libertado.<o:p></o:p></span></p>

