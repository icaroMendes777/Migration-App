<p class=Normal><a name=N1></a><a name=NA></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>[A] “Naga” é um termo empregado para descrever um
grande ser, como por exemplo um elefante ou uma grande serpente mágica. Os
Budistas adotaram esse termo como um epíteto para o Buda e para os seus
discípulos arahants. [<a href="#RA">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>[1] O
significado deste parágrafo pode ser interpretado com base no comentário da
seguinte forma: “Com relação ao que é visto houver apenas o visto” significa
que a consciência no olho vê apenas a forma como forma e não algum outro
elemento como sendo permanente, etc. Da mesma forma com relação aos outros
tipos de consciência. “Apenas” indica o limite, significando que a mente
equivale à consciência no olho sem estar afetada pela cobiça, raiva ou delusão
com relação ao objeto que foi contatado e sem adicionar as próprias idéias,
conceitos e proliferações mentais. “Você não estará ‘com aquilo’”: você não
será estimulado ‘com aquela’ cobiça, raiva, ou deludido ‘com aquela’ delusão.
“Então você não estará ‘naquilo’”: não sendo estimulado pela cobiça, etc.,
então você não estará atado, agrilhoado, estabelecido naquilo que é visto,
ouvido, sentido e conscientizado através das respectivas idéias e proliferações
mentais. “Então você não estará aqui, nem além e tampouco entre os dois”: nem
neste mundo e tampouco num mundo além, isto significa a experiência de nibbana
que está além das coisas mundanas. Veja também o <a href="SNXXXV.116.php">SN
XXXV.116</a>.<span style='mso-spacerun:yes'>  </span><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Uma
tradução alternativa para este parágrafo sugerida por Ajaan Amaro, seria:
“Quando com relação ao que é visto houver apenas o visto, ao que é ouvido houver
apenas o ouvido, ao que é sentido houver apenas o sentido, ao que é
conscientizado houver apenas o conscientizado, então, Bahiya, você deve
perceber que ali não há nada, (objeto), que aqui não há nada, (sujeito). Quando
você perceber que ali não há nada e que aqui não há nada, então você não estará
localizado nem no mundo disto, nem no mundo daquilo, tampouco em nenhum lugar
entre os dois.” Essa tradução alternativa mostra a dualidade e a não-dualidade.
<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ajaan
Brahmavamso num ensaio publicado na edição do outono de 2006 da revista
Buddhadharma oferece uma perspectiva adicional:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O Buda
repetidas vezes e de modo muito claro afirmou que a plena iluminação não pode
ser alcançada sem a experiência dos jhanas. No entanto na atualidade, alguns
mestres afirmam que o grau de desapego proporcionado pelos jhanas é
desnecessário. Com freqüência este sutta é mencionado como evidência disso.
Bahiya não era um bhikkhu. O sutta não registra que ele praticasse a
generosidade, ou que tivesse tomado refúgio na jóia tríplice, ou que observasse
os preceitos de virtude. Além disso o sutta não dá nenhuma indicação de que
Bahiya praticasse a meditação, quanto mais experimentar os jhanas. No entanto,
depois de receber um breve ensinamento do Buda, Bahiya se iluminou tornando-se
um arahant, numa questão de segundos!<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Este
episódio é bem conhecido nos círculos Budistas porque dá a impressão que a
iluminação é tão fácil. Parece que não é necessário ordenar-se como um bhikkhu,
que a pessoa pode ser egoísta sem praticar a generosidade, que não é necessário
tomar refúgio ou observar os preceitos e que até mesmo a meditação pode ser
evitada! Que alívio! Apenas a inteligência é necessária. (Todos sabemos que
somos inteligentes, não é mesmo?). Isso torna a realização de Bahiya atraente e notória. 
Será que alguém depois de ler este sutta se iluminou?
Provavelmente não. Porque não? <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Como ocorre
com freqüência, a história do sutta registra apenas os pontos principais de um
longo episódio. Da mesma forma que as fotos de um casamento não registram o
primeiro encontro, o namoro, as brigas e discussões, muitos suttas não
registram tudo que aconteceu antes. Então qual é a história completa de Bahiya?
Como podemos encaixar o final, retratado para a posteridade neste sutta, no seu
contexto apropriado? Felizmente, toda a história está registrada no Apadana
(histórias dos Budas e dos seus principais discípulos) e nos comentários.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Numa de
suas vidas passadas, Bahiya havia sido um bhikkhu sob o Buda Kassapa. Ele e
mais seis outros bhikkhus escalaram uma montanha íngreme e jogaram a escada
fora, determinados a permanecer no topo da montanha até que alcançassem a
iluminação ou morressem. Um dos bhikkhus se tornou um arahant, outro um
anagami e os cinco restantes morreram na montanha sendo que Bahiya era um
desses cinco. Na sua última vida, Bahiya havia sido um marinheiro muito viajado
e que acabou naufragando e alcançando a terra firme tendo perdido todas as suas
posses e roupas. Ele se vestiu com cascas de árvores e passou a esmolar alimentos
numa cidade chamada Supparaka. As pessoas da cidade ficaram impressionadas com
a aparência e modos de Bahiya e lhe ofereceram um modo de vida confortável e
desse forma ele não regressou ao seu ofício de marinheiro. As pessoas
consideravam que Bahiya era um arahant e Bahiya pensava da mesma forma. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Nesse ponto
um deva se deu conta do que Bahiya pensava e por compaixão o repreendeu. Esse
deva era o seu antigo companheiro na montanha que havia falecido como um
anagami. Ele relatou a Bahiya que havia um verdadeiro arahant, o Buda, que se
encontrava no outro lado da Índia, em Savatthi. De imediato Bahiya saiu de
Supparaka, (Sopara na atualidade, situada ao norte de Mumbai), levando um
noite para chegar em Savatthi.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Portanto as
circunstâncias de Bahiya eram excepcionais. Ele havia sido um bhikkhu sob o
Buda anterior, Kassapa. A sua determinação era tão forte que ele foi para a
montanha com o objetivo de se iluminar ou morrer. Na sua última vida ele podia
ouvir os devas e foi capaz de percorrer cerca de 1.300 km numa noite. Se
tivéssemos uma história como esta e os mesmos poderes supra-humanos que
Bahiya, então talvez só de ler o discurso estaríamos iluminados também.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Com certeza
Bahiya tinha a inclinação pela meditação pois para ter esse tipo de poderes é
necessário o profundo desenvolvimento dos jhanas. Mas também há outras
evidências que sugerem que Bahiya praticava os jhanas, embora isso não seja
mencionado no texto. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Poucas
pessoas sabem que o mesmo ensinamento deste sutta também foi dado pelo Buda
para o bhikkhu ancião Malunkyaputta, (<a href="SNXXXV.95.php">SN XXXV.95</a>).
Malunkyaputta aparece várias vezes nos suttas. Em particular no <a
href="MN64.php">MN 64</a>, que com certeza relata um evento anterior ao SN
XXXV.95. Nesse sutta o Buda enfatiza que os grilhões apenas são destruídos, e
como conseqüência a iluminação realizada, com o desenvolvimento dos jhanas.
Portanto o venerável Malunkyaputta foi primeiro ensinado a prática dos jhanas
para depois mais tarde receber o mesmo ensinamento que foi dado a Bahiya. Ou
seja sem os jhanas esse ensinamento não é eficaz.” [<a href="#R1">Retorna</a>]<o:p></o:p></span></p>



