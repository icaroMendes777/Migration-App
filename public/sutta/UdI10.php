<p class=Normal><a name=N3></a>[3] Bhikkhu Ñanananda no livro <i>Concept and Reality</i> explica que: nos suttas a ignorância é comparada com a escuridão e a sabedoria com a luminosidade. Estes versos portanto indicam que é precisamente porque na mente emancipada não há escuridão que o sol, a lua e as estrelas não brilham. Deve também ser notado que os verbos empregados em conexão com as estrelas, o sol e a lua significa que eles não brilham na situação em que a escuridão da ignorância foi dissipada, não que eles sejam inexistentes nesse 'plano misterioso do além'. A referência a todas as ausências, diz ele, provavelmente diz respeito ao estado de consciência do arahant em que todos os conceitos, como a terra, fogo, etc. perderam a sua susbtancialidade. [<a href="#R3">Retorna</a>]<o:p></o:p></span></p> 

