<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em certa
ocasião o Abençoado estava em Vesali na Grande Floresta no Salão com um pico na
cumeeira. Então o General Siha foi até o Abençoado e depois de cumprimentá-lo
sentou a um lado e disse: <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;É possível, senhor,
apontar um fruto da generosidade visível no aqui e agora?&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;É possível, Siha.
Quem é generoso, um mestre na generosidade, é querido e estimado pelas
pessoas em geral. E o fato de que quem é generoso, um mestre na generosidade,
ser querido e estimado pelas pessoas em geral: esse é um fruto da
generosidade visível no aqui e agora. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Além disso, boas
pessoas, pessoas íntegras, admiram quem é generoso, um mestre da generosidade.
E o fato de que boas pessoas, pessoas íntegras, admirarem quem é generoso, um
mestre na generosidade: esse, também, é um fruto da generosidade visível no
aqui e agora. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Além disso, a fina
reputação de quem é generoso, um mestre da generosidade, se difunde amplamente.
E o fato de que a fina reputação de quem é generoso, um mestre da generosidade,
se difundir amplamente: esse, também, é um fruto da generosidade visível no aqui
e agora. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Além disso, quando
alguém é generoso, um mestre da generosidade, se aproxima de uma assembléia de
pessoas - khattiyas, brâmanes, chefes de família, ou contemplativos -
ele assim o faz com confiança e sem embaraço: e o fato de que quando alguém é
generoso, um mestre da generosidade, se aproximar de uma assembléia de pessoas
- khattiyas, brâmanes, chefes de  família, ou contemplativos – fazê-lo
com confiança e sem embaraço: esse, também, é um fruto da generosidade visível
no aqui e agora. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Além disso, na
dissolução do corpo, após a morte, aquele que é generoso, um mestre da
generosidade, renasce num destino feliz, no paraíso. E o fato de que na
dissolução do corpo, após a morte, aquele que é generoso, um mestre da
generosidade, renascer num destino feliz, no paraíso: esse, também, é um fruto
da generosidade visível na próxima vida. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isto foi dito, o
general Siha disse para o Abençoado: &quot;Quanto aos quatro frutos da
generosidade visíveis no aqui e agora que foram apontados pelo Abençoado, não é
o caso de que eu os aceite pela convicção no Abençoado. Eu
os conheço também. Eu sou generoso, um mestre da generosidade, querido e
estimado pelas pessoas em geral. Eu sou generoso, um mestre da generosidade;
pessoas boas, pessoas íntegras me admiram. Eu sou generoso, um mestre da
generosidade e minha fina reputação está amplamente difundida: ‘Siha é
generoso, ele faz, apóia a Sangha.’ Eu sou generoso, um mestre da generosidade
e quando me aproximo de uma assembléia de pessoas - Khattiyas,
brâmanes, chefes de família, ou contemplativos - eu o faço com confiança e sem
embaraço.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Porém quando o
Abençoado diz, ‘Na dissolução do corpo, após a morte, aquele que é generoso,
um mestre da generosidade, renasce num destino feliz, no paraíso.’ Isso eu não
sei. Isso eu aceito com base na convicção no Abençoado.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Assim é, Siha. Assim
é. Na dissolução do corpo, após a morte, aquele que é generoso, um mestre da
generosidade, renasce num destino feliz, no paraíso.&quot; <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quem é
generoso, é querido.<br>
As pessoas em geral o admiram.<br>
Ele obtém honras. O seu status aumenta.<br>
Ele entra em uma assembléia desembaraçado.<br>
Ele é confiante – aquele que não é mesquinho. <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>Por conseguinte aquele
que é sábio provê oferendas.<br>
&nbsp;&nbsp;&nbsp; Buscando a felicidade,<br>
ele subjuga a mácula<br>
&nbsp;&nbsp;&nbsp; da mesquinharia.<br>
Estabelecido no paraíso,<br>
ele desfruta por longo tempo,<br>
em companhia dos devas. <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>Tendo criado a
oportunidade para si mesmo,<br>
tendo feito o que é hábil,<br>
então quando ele parte daqui,<br>
segue adiante, luminoso, em <a name=nandana>Nandana</a> [o jardim dos devas]. <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>Lá ele se delicia,
desfruta, feliz,<br>
pleno com os cinco elementos dos prazeres dos sentidos.<br>
Tendo seguido as palavras do Sábio que é Assim,<br>
ele desfruta do paraíso - <br>
&nbsp;&nbsp;&nbsp;o discípulo do Abençoado. <o:p></o:p></span></p>
