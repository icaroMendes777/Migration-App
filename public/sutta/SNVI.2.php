<p class=Normal><span lang=PT-BR
style='mso-ansi-language:PT-BR'>Assim ouvi. Certa ocasião, o Abençoado estava
em Uruvela às margens do rio Neranjara ao pé da figueira-dos-pagodes, pouco tempo depois de ter alcançado a perfeita iluminação. Então,
enquanto o Abençoado estava só em isolamento, um pensamento surgiu na sua mente:
“Uma pessoa sofre se permanecer sem reverência ou deferência. Agora, de qual
brâmane ou contemplativo posso depender, honrando-o e respeitando-o?&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então,
o pensamento lhe ocorreu: “Seria com o propósito de aperfeiçoar um agregado de
virtude não aperfeiçoado que eu permaneceria na dependência de um outro brâmane
ou contemplativo, honrando-o e respeitando-o? No entanto, neste mundo com os seus
devas, Mara e Brahma, nesta população com seus brâmanes e contemplativos, seus
príncipes e povo, eu não vejo outro brâmane ou contemplativo mais consumado em
virtude do que eu, ninguém na dependência do qual eu possa permanecer
honrando-o e respeitando-o.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Seria
com o propósito de aperfeiçoar um agregado de concentração não aperfeiçoado... <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Seria
com o propósito de aperfeiçoar um agregado de sabedoria não aperfeiçoado... <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Seria
com o propósito de aperfeiçoar um agregado de libertação não aperfeiçoado... <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Seria
com o propósito de aperfeiçoar um agregado de conhecimento e visão da
libertação não aperfeiçoado que eu permaneceria na dependência de um outro
brâmane ou contemplativo, honrando-o e respeitando-o? No entanto, neste mundo
com os seus devas, Mara e Brahma, nesta população com seus brâmanes e
contemplativos, seus príncipes e povo, eu não vejo outro brâmane ou
contemplativo mais consumado em conhecimento e visão da libertação do que eu,
ninguém na dependência do qual eu possa permanecer honrando-o e respeitando-o.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;E
se eu permanecesse na dependência deste mesmo Dhamma para o qual eu despertei
perfeitamente, honrando-o e respeitando-o?&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então,
tendo percebido com a sua<span style="mso-spacerun: yes">  </span>mente o
pensamento na mente do Abençoado – com a mesma rapidez com que um homem forte
pode estender o seu braço flexionado ou flexionar o seu braço estendido - o
Brahma Sahampati desapareceu do mundo de Brahma e reapareceu na frente do
Abençoado. Arrumando o seu manto externo sobre o ombro, ele juntou as mãos em
uma reverenciosa saudação dizendo: “Assim é, Abençoado! Assim é,<span
style="mso-spacerun: yes">  </span>Iluminado! Aqueles que foram Arahants,
Perfeitamente Iluminados no passado – esses Abençoados, também, permaneceram na
dependência deste mesmo Dhamma, honrando-o e respeitando-o. Aqueles que serão
Arahants, Perfeitamente Iluminados no futuro – esses Abençoados, também,
permanecerão na dependência deste Dhamma, honrando-o e respeitando-o. Que o
Abençoado, que é o Arahant no momento, o Perfeitamente Iluminado, permaneça na
dependência deste Dhamma, honrando-o e respeitando-o.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o que Brahma
Sahampati disse. Tendo dito isso, ele disse ainda mais: <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
 “Budas<span lang=PT-BR
style='mso-ansi-language:PT-BR'> do passado,<br>
Budas do futuro,<br>
e ele que agora é o Buda,<br>
removendo o sofrimento de muitos - <o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>
 “Todos permaneceram, permanecerão, permanecem,<br>
honrando profundamente o verdadeiro Dhamma.<br>
Para os Budas, <br>
essa é uma lei da natureza. <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
 “Portanto, aquele que
deseja seu próprio bem,<br>
aspirando pela grandeza espiritual,<br>
deveria honrar profundamente o verdadeiro Dhamma,<br>
recordando-se do ensinamento do Buda. <o:p></o:p></span></p>


