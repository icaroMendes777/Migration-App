<p class=Normal> “Bhikkhus, há esses quatro tipos de lagoas. Quais quatro? Aquela que é rasa porém parece profunda, aquela que é profunda porém parece rasa, aquela que é rasa e parece rasa, e aquela que é profunda e parece profunda. Esses são os quatro tipos de lagoas. </p>

<p class=Normal> “Da mesma forma, existem quatro tipos de pessoas que podem ser encontradas no mundo que se assemelham a lagoas. Quais quatro? Aquela que é rasa porém parece profunda, aquela que é profunda porém parece rasa, aquela que é rasa e parece rasa, e aquela que é profunda e parece profunda. </p>

<p class=Normal> “E como, bhikkhus, é o tipo de pessoa que é rasa porém parece profunda? É o caso em que alguém inspira confiança por sua maneira de seguir e retornar, olhar para a frente e para os lados, recolher e estender os seus membros, vestir os mantos e carregar o manto externo e a tigela de esmolar; mas ele não compreende como na verdade é: 'Isto é sofrimento,' e 'Esta é a origem do sofrimento,' e 'Esta é a cessação do sofrimento,' e 'Este é o caminho que conduz à cessação do sofrimento.' Esse é o tipo de pessoa que é rasa porém parece profunda. Esse tipo de pessoa, lhes digo, é como a lagoa que é rasa porém parece profunda. </p>

<p class=Normal> “E como é o tipo de pessoa que é profunda porém parece rasa? É o caso em que alguém não inspira confiança por sua maneira de seguir e retornar ... . vestir os mantos e carregar o manto externo e a tigela de esmolar; mas ele compreende como na verdade é: 'Isto é sofrimento,' e 'Esta é a origem do sofrimento,' e 'Esta é a cessação do sofrimento,' e 'Este é o caminho que conduz à cessação do sofrimento.' Esse é o tipo de pessoa que é profunda porém parece rasa. Esse tipo de pessoa, lhes digo, é como a lagoa que é profunda porém parece rasa. </p>

<p class=Normal> “E como é o tipo de pessoa que é rasa e parece rasa? É o caso em que alguém não inspira confiança por sua maneira de seguir e retornar ... . vestir os mantos e carregar o manto externo e a tigela de esmolar; e ele não compreende como na verdade é: 'Isto é sofrimento,' e 'Esta é a origem do sofrimento,' e 'Esta é a cessação do sofrimento,' e 'Este é o caminho que conduz à cessação do sofrimento.' Esse é o tipo de pessoa que é rasa e parece rasa. Esse tipo de pessoa, lhes digo, é como a lagoa que é rasa e parece rasa. </p>

<p class=Normal> “E como é o tipo de pessoa que é profunda e parece profunda? É o caso em que alguém inspira confiança por sua maneira de seguir e retornar ... . vestir os mantos e carregar o manto externo e a tigela de esmolar; e ele compreende como na verdade é: 'Isto é sofrimento,' e 'Esta é a origem do sofrimento,' e 'Esta é a cessação do sofrimento,' e 'Este é o caminho que conduz à cessação do sofrimento.' Esse é o tipo de pessoa que é profunda e parece profunda. Esse tipo de pessoa, lhes digo, é como a lagoa que é profunda e parece profunda. </p>

<p class=Normal> “Esses são os quatro tipos de pessoas que podem ser encontradas no mundo." </p>

