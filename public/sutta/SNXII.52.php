<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em Savathi.
“Bhikkhus, quando alguém permanece contemplando a gratificação nas coisas
passíveis de apego, o desejo aumenta. Do desejo como condição, o apego [surge].
Do apego como condição, o ser/existir. Do ser/existir como condição, o
nascimento. Do nascimento como condição, então o envelhecimento e morte,
tristeza, lamentação, dor, angústia e desespero surgem. Essa é a origem de toda
essa massa de sofrimento.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Como se
uma grande massa de dez ... vinte ... trinta ou quarenta carregamentos de
madeira estivesse queimando, e nessa fogueira um homem jogasse de tempos em
tempos um tanto de capim seco, estrume seco de vaca e madeira seca, de tal forma
que a grande fogueira, dessa forma alimentada e sustentada, queimaria por um
longo, longo tempo. Da mesma maneira, quando alguém permanece contemplando a
gratificação nas coisas passíveis de apego, o desejo aumenta. Do desejo como
condição, o apego [surge]. Do apego como condição, o ser/existir. Do
ser/existir como condição, o nascimento. Do nascimento como condição, então o
envelhecimento e morte, tristeza, lamentação, dor, angústia e desespero surgem.
Essa é a origem de toda essa massa de sofrimento.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Agora,
quando alguém permanece contemplando o perigo nas coisas passíveis de apego, o
desejo cessa. Da cessação do desejo cessa o apego. Da cessação do<span
style="mso-spacerun: yes">  </span>apego cessa o ser/existir. Da cessação
do<span style="mso-spacerun: yes">  </span>ser/existir cessa o nascimento. Da
cessação do<span style="mso-spacerun: yes">  </span>nascimento, então o
envelhecimento e morte, tristeza, lamentação, dor, angústia e desespero, tudo
cessa. Essa é a cessação de toda essa massa de sofrimento.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Como se
uma grande massa de dez ... vinte ... trinta ou quarenta carregamentos de
madeira estivesse queimando, e nessa fogueira um homem <i>não </i>jogasse de
tempos em tempos um tanto de capim seco, estrume seco de vaca e madeira seca, de
tal forma que a grande fogueira, tendo consumido o seu combustível
original,<span style="mso-spacerun: yes">  </span>sem sustentação, sem
alimento, se extinguiria. Da mesma maneira, <a name=N6></a><a name=N18></a>quando
