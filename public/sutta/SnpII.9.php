<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>Nota de
Thanissaro Bhikkhu:</b> Este discurso menciona a noção metafórica “cerne” (<i>sara</i>)
três vezes. Embora <span class=GramE><i>sara</i></span> como uma metáfora seja
com freqüência traduzida como “essência”, isto omite algumas das implicações
desta metáfora. Quando se diz <span class=GramE>que x tem y como seu cerne,
isso significa que o desenvolvimento adequado de x produz y, e que y é a parte
mais valiosa de x – tal como uma árvore, na medida em que</span> cresce,
desenvolve o cerne, e o cerne é a parte mais valiosa da árvore.<o:p></o:p></span></p>


