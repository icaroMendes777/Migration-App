<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15.
“Suponha, Magandiya, que houvesse um leproso com feridas e chagas nos membros,
sendo devorado por vermes, coçando as crostas das feridas com as unhas,
cauterizando as feridas do corpo com carvão em brasa. Então os seus amigos e
companheiros, seus pares e parentes, trouxessem um médico para tratar dele. O
médico prepararia um remédio para ele, e através desse remédio o homem ficaria
curado da lepra e estaria saudável e feliz, independente, senhor de si mesmo,
capaz de ir onde quisesse. Então dois homens fortes o agarrassem pelos dois
braços e o arrastassem na direção de uma cova com carvão em brasa. O que você
pensa, Magandiya? Aquele homem iria contorcer o corpo nesta e naquela direção?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
Mestre Gotama. Por que isso? Porque aquele fogo é de fato doloroso de tocar,
ardente e com calor intenso.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O que você
pensa, Magandiya? Apenas agora aquele fogo é doloroso de tocar, ardente e com
calor intenso, ou antes também aquele fogo era doloroso de tocar, ardente e com
calor intenso?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mestre
Gotama, aquele fogo agora é doloroso de tocar, ardente e com calor intenso, e
antes também aquele fogo era doloroso de tocar, ardente e com calor intenso.
Pois quando aquele homem era um leproso com feridas e chagas nos membros e era
devorado por vermes, coçando as crostas das feridas com as unhas, as suas
faculdades estavam debilitadas; dessa forma, embora o fogo na verdade fosse
doloroso de tocar, ele de forma equivocada o percebia como prazeroso.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16. “Da
mesma forma, Magandiya, no passado os prazeres sensuais foram dolorosos de
tocar, ardentes e com calor intenso; no futuro os prazeres sensuais serão
dolorosos de tocar, ardentes e com calor intenso; e agora no presente os
prazeres sensuais são dolorosos de tocar, ardentes e com calor intenso. Mas
esses seres que não estão livres da cobiça pelos prazeres sensuais, que estão
sendo devorados pelo desejo pelos prazeres sensuais, queimando com a febre
pelos prazeres sensuais, que estão se entregando aos prazeres sensuais, estão
com as faculdades debilitadas; dessa forma, embora os prazeres sensuais sejam
na verdade dolorosos de tocar, eles de forma equivocada os percebem como prazer.
[</span><a href="#N4"><span lang=PT-BR style='mso-ansi-language:PT-BR'>4</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=R4></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>17. “Suponha, Magandiya, que houvesse um
leproso com feridas e chagas nos membros, sendo devorado por vermes, coçando as
crostas das feridas com as unhas, cauterizando as feridas do corpo com carvão
em brasa; quanto mais ele coçasse as crostas e cauterizasse seu corpo, mais
asquerosas, mais mal cheirosas e mais infectadas ficariam as feridas, no
entanto ele descobriria uma certa dose de satisfação e prazer ao coçar suas
feridas. Da mesma forma, Magandiya, seres que não estão livres da cobiça pelos
prazeres sensuais, que estão sendo devorados pelo desejo pelos prazeres
sensuais, que estão queimando com a febre pelos prazeres sensuais, ainda se
entregando aos prazeres sensuais; quanto mais esses seres se entregarem aos
prazeres sensuais, mais aumentará a sua cobiça pelos prazeres sensuais e mais
eles serão queimados pela febre pelos prazeres sensuais, eles no entanto
descobrem uma certa dose de satisfação e prazer na dependência desses cinco
elementos do prazer sensual.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18. “O que
você pensa, Magandiya? Você alguma vez viu ou ouviu um rei, ou o ministro de um
rei, desfrutando do prazer, provido e dotado dos cinco elementos do prazer
sensual que, sem abandonar o desejo pelos prazeres sensuais, sem remover a
febre pelos prazeres sensuais, foi capaz de permanecer livre da sede, com a
mente em paz no seu interior, ou que é capaz ou que será capaz de assim
permanecer?” – “Não, Mestre Gotama.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Muito bem,
Magandiya. Eu tampouco vi ou ouvi um rei, ou o ministro de um rei, desfrutando
do prazer, provido e dotado dos cinco elementos do prazer sensual que, sem
abandonar o desejo pelos prazeres sensuais, sem remover a febre pelos prazeres
sensuais, foi capaz de permanecer livre da sede, com a mente em paz no seu
interior, ou que seja capaz, ou que será capaz de assim permanecer. Ao
contrário, Magandiya, aqueles contemplativos ou brâmanes que permaneceram ou
permanecem ou permanecerão livres da sede, com a mente em paz no seu interior,
todos assim o fazem depois de terem compreendido como na verdade é a origem, a
cessação, a gratificação, o perigo e a escapatória no caso dos prazeres
sensuais, e é depois de abandonar o desejo pelos prazeres sensuais e remover a
febre pelos prazeres sensuais que eles permanecem ou permanecerão livres da sede,
com a mente em paz no seu interior.”<o:p></o:p></span></p>



