<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11.
“Suponha, Magandiya, que um chefe de família ou o filho de um chefe de família
fosse rico, com grande riqueza e grandes posses, e tendo sido provido e dotado
dos cinco elementos do prazer sensual, ele desfrutasse do prazer com as formas
percebidas pelo olho… com os sons percebidos pelo ouvido… com os aromas
percebidos pelo nariz… com os sabores percebidos pela língua… com os tangíveis
percebidos pelo corpo que são desejáveis, agradáveis e fáceis de serem
gostados, conectados com o desejo sensual e que provocam a cobiça. Tendo boa
conduta com o corpo, linguagem e mente, na dissolução do corpo, após a morte,
ele renasce num destino feliz, no paraíso, em companhia dos devas do Trinta e
três; e lá, cercado por um grupo de ninfas do Bosque de Nandana, ele desfruta o
prazer, provido e dotado dos cinco elementos do prazer sensual divino. Suponha
que ele visse um chefe de família ou o filho de um chefe de família desfrutando
o prazer, provido e dotado dos cinco elementos [humanos] do prazer sensual. O
que você pensa, Magandiya? Aquele jovem deva cercado pelo grupo de ninfas do
Bosque de Nandana, desfrutando o prazer, provido e dotado dos cinco elementos
do prazer sensual divino, invejaria o chefe de família ou o filho do chefe de
família devido aos cinco elementos do prazer sensual humano, ele retornaria ao
prazer sensual humano?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
Mestre Gotama. Por que não? Porque o prazer sensual divino é mais excelente e
sublime que os prazeres sensuais humanos.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. “Da
mesma forma, Magandiya, no passado quando vivia a vida em família, eu
desfrutava o prazer, provido e dotado dos cinco elementos do prazer sensual:
com as formas percebidas pelo olho… com os sons percebidos pelo ouvido… com os
aromas percebidos pelo nariz… com os sabores percebidos pela língua… com os
tangíveis percebidos pelo corpo que são desejáveis, agradáveis e fáceis de
serem gostados, conectados com o desejo sensual e que provocam a cobiça. Mais
tarde, tendo compreendido como na verdade é a origem, a cessação, a
gratificação, o perigo e a escapatória no caso das formas, eu abandonei o
desejo pelas formas, removi a febre pelas formas e permaneci sem sede, com a
mente em paz no seu interior. Eu vejo outros seres que não estão livres da
cobiça pelos prazeres sensuais sendo devorados pelo desejo pelos prazeres
sensuais, queimando com a febre pelos prazeres sensuais, se entregando aos
prazeres sensuais, e eu não os invejo, nem sinto prazer naquilo. Por que isso?
Porque existe um deleite, Magandiya, que está afastado dos prazeres sensuais,
afastado de estados prejudiciais, que ultrapassa até mesmo o prazer divino. Como eu
me deleito nisso, não invejo o que é inferior, nem sinto prazer naquilo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13.
“Suponha, Magandiya, que houvesse um leproso com feridas e chagas nos membros,
sendo devorado por vermes, coçando as crostas das feridas com as unhas,
cauterizando as feridas do corpo com carvão em brasa. Então os seus amigos e
companheiros, seus pares e parentes, trouxessem um médico para tratar dele. O
médico prepararia um remédio para ele, e através desse remédio o homem ficaria
curado da lepra e estaria saudável e feliz, independente, senhor de si mesmo,
capaz de ir onde quisesse. Então, ele talvez visse um outro leproso com feridas
e chagas nos membros, sendo devorado por vermes, coçando as crostas das feridas
com as unhas, cauterizando as feridas do corpo com carvão em brasa. O que você
pensa, Magandiya? Aquele homem invejaria aquele leproso pelo seu carvão em
brasas ou pelos remédios que ele estivesse usando?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
Mestre Gotama. Por que isso? Porque quando há enfermidade, o remédio tem que ser
usado, e quando não há enfermidade o remédio não tem que ser usado.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14. “Da
mesma forma, Magandiya, no passado quando eu vivia a vida em família... (igual
ao verso12)... Como eu me deleito nisso, não invejo o que é inferior, nem sinto
prazer naquilo.<o:p></o:p></span></p>

