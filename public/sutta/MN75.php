<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>‘O melhor de todos os ganhos é a saúde,<br>

Nibbana é a felicidade máxima’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Nós também
ouvimos isso ser dito por errantes do passado na tradição dos mestres, e é a
mesma coisa, Mestre Gotama.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas,
Magandiya, quando você ouviu isso ser dito por errantes do passado na tradição
dos mestres, o que é a saúde, o que é Nibbana?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isso
foi dito, o errante Magandiya esfregou os membros com as mãos e disse: “Isto é
saúde, Mestre Gotama, isto é Nibbana; pois eu agora estou saudável e feliz e
nada me aflige.” [</span><a href="#N5"><span lang=PT-BR style='mso-ansi-language:
PT-BR'>5</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=R5></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>20. “Magandiya, suponha que houvesse um homem cego que não pudesse
enxergar as formas escuras e claras, que não pudesse enxergar as formas azuis,
amarelas, vermelhas ou rosa, que não pudesse enxergar o que fosse regular ou
irregular, não pudesse enxergar as estrelas ou o sol e a lua. Pode ser que ele
ouvisse um homem com boa visão dizendo: ‘Bom, senhores, deveras é um tecido
branco, belo, imaculado e limpo!’ e ele sairia em busca de um tecido branco.
Então um homem o trapacearia com um tecido sujo, manchado, assim: ‘Bom homem,
aqui está para você um tecido branco, belo, imaculado e limpo.’ E ele o
aceitaria e vestiria, e estando satisfeito diria estas palavras de satisfação:
‘Bom, senhores, deveras é um tecido branco, belo, imaculado e limpo!’ O que
você pensa, Magandiya? Quando aquele homem cego de nascença aceitou aquele
tecido sujo, manchado, vestiu-o e satisfeito disse estas palavras de
satisfação: ‘Bom, senhores, deveras é um tecido branco, belo, imaculado e
limpo!’ – ele assim o fez sabendo e vendo, ou por fé no homem com boa visão?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“&quot;Venerável
senhor, ele teria feito isso sem saber e sem ver, por fé no homem com boa
visão.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>21. “Da
mesma forma, Magandiya, os errantes de outras seitas são cegos e sem visão.
Eles não sabem o que é saúde, eles não vêm Nibbana, apesar disso proclamam
estes versos:<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>‘O melhor de todos os ganhos é a saúde,<br>

Nibbana é a felicidade máxima’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Estes
versos foram proclamados pelos Abençoados, Perfeitamente Iluminados do passado:<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>‘O melhor de todos os ganhos é a saúde,<br>

Nibbana é a felicidade máxima,<br>

o caminho óctuplo é o melhor dos caminhos,<br>

pois ele conduz com segurança ao imortal.’<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Agora, aos
poucos, esses versos se tornaram conhecidos entre as pessoas comuns. [</span><a
href="#N6"><span lang=PT-BR style='mso-ansi-language:PT-BR'>6</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R6></a>E embora este
corpo, Magandiya, seja uma enfermidade, um tumor, uma flecha, uma calamidade e
uma aflição, referindo-se a este corpo você diz: ‘Isto é saúde, Mestre Gotama,
isto é Nibbana.’ Você não tem aquela nobre visão, Magandiya, através da qual
você possa saber o que é saúde e ver Nibbana.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>22. “Eu
tenho confiança no Mestre Gotama assim: ‘Mestre Gotama é capaz de ensinar-me o
Dhamma de tal forma que eu possa vir a saber o que é saúde e ver Nibbana.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Magandiya,
suponha que houvesse um homem cego que não pudesse enxergar as formas escuras e
claras... ou o sol e a lua. Então, os seus amigos e companheiros, seus pares e
parentes, trouxessem um médico para tratar dele. O médico lhe prepararia um
remédio e através desse remédio a visão do homem não surgiria e não se
purificaria. O que você pensa, Magandiya, aquele médico iria obter apenas fadiga
e enervação?” – “Sim, Mestre Gotama.” – “Da mesma forma, Magandiya, se eu lhe
ensinasse o Dhamma desta forma: ‘Isto é saúde, isto é Nibbana,’ você poderá não
saber o que é saúde e poderá não ver Nibbana e isso seria fatigante e
problemático para mim.”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>23. “Eu
tenho confiança no Mestre Gotama assim: ‘Mestre Gotama é capaz de ensinar-me o
Dhamma de tal forma que eu possa vir a saber o que é saúde e ver Nibbana.’” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Magandiya,
suponha que houvesse um homem cego que não pudesse enxergar as formas escuras e
claras... ou o sol e a lua. Pode ser que ele ouvisse um homem com boa visão
dizendo: ‘Bom, senhores, deveras é um tecido branco, belo, imaculado e limpo!’
e ele sairia em busca de um tecido branco. Então um homem o trapacearia com um
tecido sujo, manchado, assim: ‘Bom homem, aqui está para você um tecido branco,
belo, imaculado e limpo.’ E ele o aceitaria e vestiria. Então os seus amigos e
companheiros, seus pares e parentes, trariam um médico para tratar dele. O
médico prepararia um remédio – eméticos e purgativos, pomadas e contra-pomadas
e tratamento nasal – e através desse remédio a visão do homem surgiria e se
purificaria. Junto com o surgimento da sua visão, o desejo e apreço por aquele
tecido sujo e manchado seriam abandonados; então é possível que ele ardesse de
indignação e inimizade em relação aquele homem e é possível que ele pensasse
que deveria mat­á-lo assim: ‘De fato, durante muito tempo eu fui enganado,
trapaceado e defraudado por esse homem com este tecido sujo e manchado quando
ele me disse: “Bom homem, aqui está para você um tecido branco, belo, imaculado
e limpo.’”<o:p></o:p></span></p>



<p class=Normal><a name=T24></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>24. “Da mesma forma, Magandiya, se eu lhe ensinasse o Dhamma desta
forma: ‘Isto é saúde, isto é Nibbana,’ é possível que você saiba o que é saúde
e veja Nibbana. Junto com o surgimento da sua visão, é possível que o desejo e
cobiça pelos cinco agregados influenciados pelo apego seja abandonado. Então,
talvez você pense: ‘De fato, durante muito tempo eu fui enganado, trapaceado e
defraudado por esta mente. Pois ao me apegar, eu estava me apegando apenas à
forma material, eu estava me apegando apenas à sensação, eu estava me apegando
apenas à percepção, eu estava me apegando apenas às formações, eu estava<span
style='mso-spacerun:yes'>  </span>me apegando apenas à consciência. [</span><a
href="#N7"><span lang=PT-BR style='mso-ansi-language:PT-BR'>7</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R7></a>Com o meu apego
como condição, ser/existir [surge]; com o ser/existir como condição,
nascimento; com o nascimento como condição, envelhecimento e morte, tristeza,
lamentação, dor, angústia e desespero surgem. Essa é a origem de toda essa
massa de sofrimento.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>25. “Eu
tenho confiança no Mestre Gotama assim: ‘Mestre Gotama é capaz de ensinar-me o
Dhamma de tal forma que eu possa me levantar do meu assento curado da minha
cegueira.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
Magandiya, associe-se com homens verdadeiros. Se você se associar com homens
verdadeiros, irá ouvir o Dhamma verdadeiro. Ao ouvir o Dhamma verdadeiro, você
irá praticar de acordo com o Dhamma verdadeiro. Quando você pratica de acordo
com o Dhamma verdadeiro, você irá conhecer e ver por você mesmo assim: ‘Essas
são enfermidades, tumores e flechas; mas aqui essas enfermidades, tumores e
flechas cessam sem deixar vestígios. [</span><a href="#N8"><span lang=PT-BR
style='mso-ansi-language:PT-BR'>8</span></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>] <a name=R8></a>Com a cessação do meu apego, cessa o ser/existir; com a
cessação do ser/existir, cessa o nascimento; com a cessação do nascimento,
envelhecimento, morte, tristeza, lamentação, dor, angústia e desespero tudo
cessa. Essa é a cessação de toda essa massa de sofrimento.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>26. Quando
isso foi dito o errante Magandiya disse: &quot;Magnífico, Mestre Gotama!
Magnífico, Mestre Gotama! Mestre Gotama esclareceu o Dhamma de várias formas,
como se tivesse colocado em pé o que estava de cabeça para baixo, revelasse o
que estava escondido, mostrasse o caminho para alguém que estivesse perdido ou
segurasse uma lâmpada no escuro para que aqueles que possuem visão possam ver
as formas. Eu busco refúgio no Mestre Gotama, no Dhamma e na Sangha dos
bhikkhus. Eu receberia a admissão na vida santa sob o Abençoado e a admissão
completa.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>27.
“Magandiya, alguém que anteriormente tenha pertencido a uma outra seita e que
quer ser admitido na vida santa e a admissão completa neste Dhamma e Disciplina
terá um período de noviciado de quatro meses. <a name=R10></a>Ao final dos
quatro meses, se os bhikkhus estiverem satisfeitos com ele, eles lhe darão a
admissão na vida santa e também a admissão completa como bhikkhu. Mas eu
reconheço diferenças entre indivíduos neste assunto.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Venerável
senhor, se aqueles que pertenceram anteriormente a uma outra seita e querem a
admissão na vida santa e a admissão completa<span style='mso-spacerun:yes'> 
</span>nesse Dhamma e Disciplina vivem como noviços durante quatro meses e ao
final dos quatro meses os bhikkhus que estiverem satisfeitos com ele lhe darão
admissão na vida santa e também a admissão completa como bhikkhu, eu viverei
como noviço durante quatro anos. Ao final dos quatro anos, se os bhikkhus
estiverem<span style='mso-spacerun:yes'>  </span>satisfeitos, que me dêem a
admissão na vida santa e a admissão completa como bhikkhu&quot; <span
style='color:red'><o:p></o:p></span></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>28. Então o
errante Magandiya recebeu a admissão na vida santa sob o Abençoado e ele
recebeu a admissão completa como bhikkhu. E depois da sua admissão completa,
permanecendo só, isolado, diligente, ardente e decidido, em pouco tempo, ele
alcançou e permaneceu no objetivo supremo da vida santa pelo qual membros de um
clã deixam a vida em família pela vida santa, tendo conhecido e realizado por
si mesmo no aqui e agora. Ele soube: “O nascimento foi destruído, a vida santa
foi vivida, o que deveria ser feito foi feito, não há mais vir a ser a nenhum
estado.”<span style='mso-spacerun:yes'>  </span>E o venerável Magandiya se
tornou mais um dos arahants.<span style='mso-spacerun:yes'>  </span><o:p></o:p></span></p>

