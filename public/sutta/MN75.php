<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim ouvi.
Certa ocasião o Abençoado estava no país dos Kurus em uma cidade Kuru
denominada Kammasadhamma, num leito de capim no cômodo com lareira de um
brâmane pertencente ao clã Bharadvaja.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. Então,
ao amanhecer, o Abençoado se vestiu e tomando a tigela e o manto externo, foi
para Kammasadhamma esmolar alimentos. Depois de haver esmolado em Kammasadhamma
e de haver retornado, após a refeição ele foi até um certo bosque para passar o
resto do dia. Tendo entrado no bosque, ele sentou à sombra de uma árvore.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. Então o
errante Magandiya, enquanto caminhava e perambulava fazendo exercício, foi até
o cômodo com lareira do brâmane pertencente ao clã Bharadvaja. Lá, ele viu um
leito preparado com capim<span style='mso-spacerun:yes'>  </span>e perguntou ao
brâmane: “Para quem foi preparado esse leito com capim no cômodo com lareira do
Mestre Bharadvaja? Parece a cama de um contemplativo.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. “Mestre
Magandiya, é para o contemplativo Gotama, o filho dos Sakyas, que adotou a vida
santa deixando o clã dos Sakyas. E acerca desse mestre Gotama existe essa boa
reputação: ‘Esse Abençoado é um arahant, perfeitamente iluminado, consumado
no verdadeiro conhecimento e conduta, bem-aventurado, conhecedor dos mundos, um
líder insuperável de pessoas preparadas para serem treinadas, mestre de devas e
humanos, desperto, sublime.’ Esse leito foi preparado para o Mestre Gotama.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. “Sem
dúvida, Mestre Bharadvaja, é uma visão ruim quando vemos a cama daquele
destruidor do crescimento, [</span><a href="#N1"><span lang=PT-BR
style='mso-ansi-language:PT-BR'>1</span></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>] <a name=R1></a>Mestre Gotama.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Seja
cuidadoso com aquilo que você diz, Magandiya, seja cuidadoso com aquilo que
você diz! Muitos nobres sábios, brâmanes sábios, chefes de família sábios e
contemplativos sábios têm plena confiança no Mestre Gotama e foram treinados
por ele no nobre caminho, no Dhamma que é benéfico.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mestre
Bharadvaja, mesmo se víssemos esse Mestre Gotama cara a cara, diríamos na cara
dele: ‘O contemplativo Gotama é um destruidor do crescimento.’ Por que isso?
Porque isso está registrado nas nossas escrituras.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se o
Mestre Magandiya não tiver objeção, posso relatar isso ao Mestre Gotama?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Que o
Mestre Bharadvaja fique tranqüilo. Conte aquilo que eu acabei de dizer.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. Enquanto
isso, com o ouvido divino, que é purificado e ultrapassa o humano, o Abençoado
ouviu essa conversa entre o brâmane do clã<span style='mso-spacerun:yes'> 
</span>Bharadvaja e o errante Magandiya. Então, ao anoitecer o Abençoado se
levantou da meditação, foi para o cômodo com lareira do brâmane e sentou-se na
cama com capim que havia sido preparada para ele. Então o brâmane do clã
Bharadvaja foi até o Abençoado e ambos se cumprimentaram. Quando a conversa
amigável e cortês havia terminado ele<span style='mso-spacerun:yes'> 
</span>sentou a um lado. O Abençoado perguntou: “Bharadvaja, você teve alguma
conversa com o errante Magandiya acerca desta cama com capim?” Quando isso foi
dito, o brâmane, espantado e com os cabelos em pé, respondeu: “Queríamos contar
ao Mestre Gotama exatamente sobre isso, mas o Mestre Gotama se antecipou.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. Mas essa
conversa entre o Abençoado e o brâmane do clã Bharadvaja não foi concluída pois
então o errante Magandiya, enquanto caminhava e perambulava fazendo exercício,
chegou no cômodo com lareira do brâmane e se dirigiu ao Abençoado. Ambos se
cumprimentaram e quando a conversa cortês e amigável havia terminado, ele
sentou a um lado. O Abençoado disse:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8.
“Magandiya, o olho sente prazer com as formas, goza com as formas, se delicia
com as formas; isso foi domado, guardado, protegido e contido pelo Tathagata, e
ele ensina o Dhamma para a sua contenção. Foi com referência a isso que você disse: ‘O contemplativo Gotama é um
destruidor do crescimento’?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Foi com
referência a isso, Mestre Gotama, que eu disse: ‘O contemplativo Gotama é um
destruidor do crescimento.’ Por que isso? Porque isso está registrado nas nossas
escrituras.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O ouvido
sente prazer com os sons... O nariz sente prazer com os aromas... A língua
sente prazer com os sabores... O corpo sente prazer com os tangíveis... A mente
sente prazer com os objetos mentais, goza com os objetos mentais, se delicia
com os objetos mentais; isso foi domado, guardado, protegido e contido pelo Tathagata, e
ele ensina o Dhamma para a sua contenção. Foi com referência a
isso que você disse: ‘O contemplativo Gotama é um destruidor do crescimento’?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Foi com
referência a isso, Mestre Gotama, que eu disse: ‘O contemplativo Gotama é um
destruidor do crescimento’ Por que isso? Porque isso está registrado nas nossas
escrituras.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. “O que
você pensa, Magandiya? Aqui, alguém pode no passado ter desfrutado o prazer
através das formas percebidas pelo olho que são desejáveis, agradáveis e fáceis
de serem gostadas, conectadas com o desejo sensual e que provocam a cobiça.
Mais tarde, tendo compreendido como na verdade é a origem, a cessação, a
gratificação, o perigo e a escapatória no caso das formas, ele poderá abandonar
o desejo pelas formas, remover a febre pelas formas e permanecer sem sede, com
a mente em paz no seu interior. O que você diria para ele, Magandiya?” – “Nada,
Mestre Gotama.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O que você
pensa, Magandiya? Aqui, alguém pode no passado ter desfrutado o<span
style='mso-spacerun:yes'>  </span>prazer através dos sons percebidos pelo
ouvido… dos aromas percebidos pelo nariz… sabores percebidos pela língua…
tangíveis percebidos pelo corpo que são desejáveis, agradáveis e fáceis de
serem gostados, conectados com o desejo sensual e que provocam a cobiça. Mais
tarde, tendo compreendido como na verdade é a origem, a cessação, a
gratificação, o perigo e a escapatória no caso dos tangíveis, ele poderá
abandonar o desejo pelos tangíveis, remover a febre pelos tangíveis e
permanecer sem sede, com a mente em paz no seu interior. O que você diria para
ele, Magandiya?” – “Nada, Mestre Gotama.”<o:p></o:p></span></p>



<p class=Normal><a name=T10></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>10. “Magandiya, no passado quando vivia a vida em família, eu desfrutava
o prazer provido e dotado dos cinco elementos do prazer sensual: com as formas
percebidas pelo olho… com os sons percebidos pelo ouvido… com os aromas
percebidos pelo nariz… com os sabores percebidos pela língua… com os tangíveis
percebidos pelo corpo, que são desejáveis, agradáveis e fáceis de serem
gostados, conectados com o desejo sensual e que provocam a cobiça. Eu tinha
três palácios, um para a estação das chuvas, um para o inverno e um para o
verão. Eu vivia no palácio para a estação das chuvas durante os quatro meses da
estação das chuvas, desfrutando o prazer com músicos que eram todas mulheres, e
eu não ia até o palácio que estava mais abaixo.[</span><a href="#N2"><span
