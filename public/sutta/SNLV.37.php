<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em certa
ocasião, o Abençoado estava<span style="mso-spacerun: yes">  </span>entre os
Sakyas em Kapilavatthu, no Parque de Nigrodha. Então o Sakya Mahanama foi até o
Abençoado e depois de cumprimentá-lo sentou a um lado e disse:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, de que modo alguém é um discípulo leigo?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando,
Mahanama, ele buscou refúgio no Buda, no Dhamma e na Sangha, ele é então um
discípulo leigo.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“De que
modo, venerável senhor, um discípulo leigo é consumado em virtude?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando,
Mahanama, um discípulo leigo se abstém de tirar a vida de outros seres, de tomar
aquilo que não é dado, da conduta sexual imprópria, da linguagem mentirosa, do vinho,
álcool e outros embriagantes que são a causa para a negligência, o discípulo
leigo é consumado em virtude.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“De que
modo, venerável senhor, um discípulo leigo é consumado na fé?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
Mahanama, um discípulo leigo é uma pessoa com fé. Ele deposita fé na iluminação
do <a name=PVW>Tathagata</a> assim: ‘O Abençoado é um arahant,
perfeitamente iluminado, consumado no verdadeiro conhecimento e conduta,
bem-aventurado, conhecedor dos mundos, um líder insuperável de pessoas
preparadas para serem treinadas, mestre de devas e humanos, desperto,
sublime.’ Desse modo
um discípulo leigo é consumado na fé.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“De que
modo, venerável senhor, um discípulo leigo é consumado na generosidade?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
Mahanama, um discípulo leigo permanece em casa com uma mente desprovida da
mácula da avareza, espontaneamente generoso, mão aberta, que se delicia com a
renúncia, devotado à caridade, deliciando-se em dar e compartir. Desse modo um
discípulo leigo é consumado na generosidade.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“De que modo,
venerável senhor, um discípulo leigo é consumado na sabedoria?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
Mahanama, um discípulo leigo é sábio, ele possui a completa compreensão da origem
e cessação, que é nobre e penetrante, conduzindo à completa destruição do
sofrimento. Desse modo um discípulo leigo é consumado na sabedoria.” <o:p></o:p></span></p>


