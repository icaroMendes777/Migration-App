<p class=Normal> Assim, ouvi. Certa ocasião, o Abençoado estava em Savatthi, no Bosque de Jeta, no Parque de Anathapindika. Agora, naquela ocasião, o único filho amado e querido de um certo discípulo leigo havia morrido. Então um número de discípulos leigos com as roupas e cabelos molhados foram até o Abençoado no meio do dia e depois de cumprimentá-lo eles sentaram a um lado. Estando ali sentados o Abençoado disse:</p>

<p class=Normal> "Por que vocês vieram aqui no meio do dia com as roupas e os cabelos molhados?"</p>

<p class=Normal> Quando isso foi dito um dos discípulos leigos respondeu para o Abençoado: "Meu único filho amado e querido, venerável senhor, morreu. Por isso viemos até aqui no meio do dia com as roupas e os cabelos molhados."</p>

<p class=Normal>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p>

<p class=Normal style='margin-left:.75in'> Devas e a maioria dos seres humanos de fato são prisioneiros <br>
daquilo que consideram como queridos e prazerosos. <br>
Exauridos pela dor (quando as pessoas queridas morrem), <br>
eles se entregam ao poder do Senhor da Morte.</p>

<p class=Normal style='margin-left:.75in'> Mas aqueles que permanecem diligentes dia e noite, <br>
que abandonam tudo que é querido -<br>
o engodo da morte difícil de ser vencido -<br>
eliminam a raiz da dor.</p>

