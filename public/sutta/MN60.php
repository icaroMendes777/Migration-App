elogiar a si mesmo e menosprezar os outros –<span class=GramE><span
style='mso-spacerun:yes'>  </span></span>todos esses estados ruins e
prejudiciais surgem, portanto, tendo o entendimento incorreto como sua
condição.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17. (A.iii)
“Com relação a isso um homem sábio considera da seguinte forma: ‘Se não existe
a ação, então na dissolução do corpo essa pessoa terá assegurado o seu bem
estar. Mas se existir a ação, então na dissolução do corpo, após a morte, ela
irá renascer num estado de privação, num destino infeliz, nos reinos
inferiores, até mesmo no <span style='mso-bidi-font-weight:bold'>inferno</span>.
Agora, sendo ou não verdadeiras as palavras daqueles<span class=GramE> <span
