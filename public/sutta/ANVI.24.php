<p class=Normal><b>Notas:</b></p>


<p class=Normal><a name=N1></a> [1] Os comentários explicam: <i>alcançar a concentração</i>: ele tem habilidade em entrar em concentração sabendo quais são as condições adequadas. <i>Duração da concentração</i>: hábil em estabilizar a concentração. <i>Emergir da concentração</i>: sair da concentração depois de um tempo estabelecido. <i>Doçura da concentração</i>: hábil em contentar a mente para a concentração. <i>Domínio da concentração</i>: evitando aquelas coisas que não são favoráveis e dedicando-se àquelas coisas que são favoráveis. <i>Resolução da concentração</i>: de modo a alcançar níveis cada vez mais profundos de concentração.  </p>

