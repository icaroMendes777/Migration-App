<p class=Normal> "Bhikkhus, possuindo seis qualidades um bhikkhu poderá rachar o Himalaia, o rei das montanhas, quanto mais então a vil ignorância! Quais seis? Aqui, um bhikkhu é hábil em alcançar a concentração; (2) hábil na duração da concentração; (3) hábil em emergir da concentração; (4) hábil na doçura da concentração; (5) hábil no domínio da concentração; (6) hábil na resolução da concentração. [<a href="#N1">1</a>] Possuindo essas seis qualidades um bhikkhu poderá rachar o himalaia, o rei das montanhas, quanto mais então a vil ignorância."</p>


