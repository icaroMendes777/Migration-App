<p class=Normal> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: "Ao se referir a um bhikkhu que pratica de acordo com o Dhamma, esta é a maneira apropriada de definir a 'prática de acordo com o Dhamma': ao falar ele fala apenas o Dhamma, não o não-Dhamma. Ao pensar ele pensa apenas pensamentos do Dhamma, não pensamentos de não-Dhamma. Evitando esses dois ele permanece equânime, com atenção plena e plena consciência."</p>

<p class=Normal style='margin-left:.75in'> Um bhikkhu que aprecia o Dhamma<br> 
e que se delicia com o Dhamma, <br>
refletindo sobre o Dhamma, <br>
não decai do verdadeiro Dhamma.</p>

<p class=Normal style='margin-left:.75in'> Quer seja caminhando ou em pé, <br>
sentado ou deitado, <br>
com a mente contida internamente, <br>
