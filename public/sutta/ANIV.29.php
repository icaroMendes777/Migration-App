<p class=Normal> "Bhikkhus, há esses quatro fatores do Dhamma, primários, duradouros, tradicionais, antigos, inalduterados e que nunca foram adulterados antes, que não estão sendo adulterados e que não serão adulterados, que não são rechaçados por contemplativos e brâmanes sábios. Quais quatro? </p>

<p class=Normal> (1) "Não-cobiça é um fator do Dhamma, primário, duradouro, tradicional, antigo, inalduterado e que nunca foi adulterado antes, que não está sendo adulterado e que não será adulterado, que não é rechaçado por contemplativos e brâmanes sábios. (2) Boa vontade é um fator do Dhamma, primário, duradouro, tradicional, antigo, inalduterado e que nunca foi adulterado antes, que não está sendo adulterado e que não será adulterado, que não é rechaçado por contemplativos e brâmanes sábios. (3) Atenção Plena Correta é um fator do Dhamma, primário, duradouro, tradicional, antigo, inalduterado e que nunca foi adulterado antes, que não está sendo adulterado e que não será adulterado, que não é rechaçado por contemplativos e brâmanes sábios. (4) Concentração Correta é um fator do Dhamma, primário, duradouro, tradicional, antigo, inalduterado e que nunca foi adulterado antes, que não está sendo adulterado e que não será adulterado, que não é rechaçado por contemplativos e brâmanes sábios. </p>

<p class=Normal>"Essas são os quatro fatores do Dhamma, primários, duradouros, tradicionais, antigos, inalduterados e que nunca foram adulterados antes, que não estão sendo adulterados e que não serão adulterados, que não são rechaçados por contemplativos e brâmanes sábios." </p>

