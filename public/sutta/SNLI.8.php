<p class=Normal> “Bhikkhus, há essas quatro bases do poder espiritual. Quais quatro? Neste caso, bhikkhus, um bhikkhu desenvolve a base do poder espiritual que possui concentração devido ao desejo e às formações volitivas do esforço. Ele desenvolve a base do poder espiritual que possui concentração devido à energia e às formações volitivas do esforço. Ele desenvolve a base do poder espiritual que possui concentração devido à mente e às formações volitivas do esforço. Ele desenvolve a base do poder espiritual que possui  concentração devido à investigação e às formações volitivas do esforço. Essas são as quatro bases do poder espiritual. Porque ele desenvolveu e cultivou essas quatro bases do poder espiritual é que o Tathagata é chamado o Arahant, o Perfeitamente Iluminado.”</p>


