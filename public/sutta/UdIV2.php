<p class=Normal>Assim ouvi. Em certa ocasião o Abençoado estava em Kusinara em Upavattana, no bosque das árvores sal dos Mallas.</p>



<p class=Normal>Então, naquela ocasião inúmeros bhikkhus viviam em uma pequena cabana na floresta, não muito distante do Abençoado, agitados, descuidados, rudes, avoados, falantes, loquazes, confusos, distraídos, sem foco, com as faculdades descontroladas.</p>



<p class=Normal>O Abençoado viu então aqueles inúmeros bhikkhus vivendo em uma pequena cabana na floresta, não muito distante do Abençoado, agitados, descuidados, rudes, avoados, falantes, loquazes, confusos, distraídos, sem foco, com as faculdades descontroladas.</p>



<p class=Normal>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p>



	<p class=Normal style='margin-left:.75in'>"É através do descontrole do corpo,<br>

	do entendimento incorreto,<br>

	e da subordinação ao torpor e à preguiça,<br>

	que ocorre a dominação por Mara.<br>

	Portanto,<br>

	com a mente protegida,<br>

	fomentando a intenção correta,<br>

	devoto ao entendimento correto,<br>

	compreendendo o surgir e desaparecer,<br>

	conquistando a preguiça e torpor,<br> 

	um bhikkhu deixa para trás<br> 

	todos os destinos infelizes.”</p>


