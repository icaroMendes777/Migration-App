<p class=Normal> Então, dois brâmanes foram até o Abençoado e eles se cumprimentaram, quando a conversa amigável e cortês havia terminado eles sentaram a um lado e disseram: </p>

<p class=Normal> "Mestre Gotama, Purana Kassapa, declara ser onisciente e capaz de ver tudo, reivindica ter conhecimento completo e visão desta forma: ‘Quer eu esteja caminhando. ou em pé, ou dormindo, ou desperto, o conhecimento e visão estão presentes em mim de forma contínua e ininterrupta.’ Ele diz: 'Com conhecimento infinito eu permaneço conhecendo e vendo o mundo como infinito.' Mas o Nigantha Nataputta também declara ser onisciente e capaz de ver tudo, reivindica ter conhecimento completo e visão desta forma: ‘Quer eu esteja caminhando, ou em pé, ou dormindo, ou desperto, o conhecimento e visão estão presentes em mim de forma contínua e ininterrupta.’ Ele diz: 'Com conhecimento infinito eu permaneço conhecendo e vendo o mundo como finito.' Quando esses dois que reivindicam ter conhecimento fazem afirmações que são contraditórias, quem diz a verdade e quem diz o que é falso?"

<p class=Normal> "Já basta, brâmanes, deixem isso de lado: 'Quando esses dois que reivindicam ter conhecimento fazem afirmações que são contraditórias, quem diz a verdade e quem diz o que é falso?' Eu lhes ensinarei o Dhamma. Ouçam e prestem muita atenção àquilo que eu vou dizer.” – “Sim, venerável senhor,” os brâmanes responderam. O Abençoado disse o seguinte: </p>

<p class=Normal> "Suponham, brâmanes, que houvesse quatro homens em pé nos quatro quadrantes possuidores de movimentação suprema, e velocidade e passada supremas. A velocidade deles seria tal, que eles poderiam se mover tão rápido quanto uma flecha de luz que atravessasse a sombra de uma palmeira atirada por um arqueiro bem treinado, habilidoso, experimentado. A passada deles seria tal, que poderiam alcançar do oceano do leste ao oceano do oeste. Então o homem parado no quadrante do leste diria: ‘Eu alcançarei o fim do mundo viajando.’ Possuindo tamanha velocidade e tamanha passada, e tendo um tempo de vida de cem anos, vivendo por cem anos, ele viajou por cem anos, sem pausa exceto para comer, beber, defecar, urinar e dissipar a fadiga; no entanto, ele morreu no meio do caminho sem ter chegado ao fim do mundo. Então o homem parado no quadrante do oeste diria ... o homem parado no quadrante do norte diria ... o homem parado no quadrante do sul diria: ‘Eu alcançarei o fim do mundo viajando.’ Possuindo tamanha velocidade e tamanha passada, e tendo um tempo de vida de cem anos, vivendo por cem anos, ele viajou por cem anos, sem pausa exceto para comer, beber, defecar, urinar e dissipar a fadiga; no entanto, ele morreu no meio do caminho sem ter chegado ao fim do mundo. Por qual razão?  Eu digo, brâmanes, que viajando alguém não será capaz de conhecer, ou ver, ou chegar ao fim do mundo. No entanto eu digo que sem ter alcançado o fim do mundo não há como dar um fim ao sofrimento. </p>

<p class=Normal> "Esses cinco objetos do prazer sensual, brâmanes, são chamados de 'mundo' na disciplina dos nobres. Quais cinco? Formas percebidas pelo olho que são desejáveis, agradáveis e fáceis de serem gostadas, conectadas com o desejo sensual e que provocam a cobiça. Sons percebidos pelo ouvido ... Aromas percebidos pelo nariz ... Sabores percebidos pela língua ... Tangíveis percebidos pelo corpo que são desejáveis, agradáveis e fáceis de serem gostados, conectados com o desejo sensual e que provocam a cobiça. Esses cinco objetos do prazer sensual são chamados de 'mundo' na disciplina dos nobres. </p>

<p class=Normal> <a name=R1></a> (1) "Aqui, brâmanes, afastado dos prazeres sensuais, afastado das qualidades não hábeis, um bhikkhu entra e permanece no primeiro jhana ... Ele é chamado um bhikkhu que, tendo chegado ao fim do mundo permanece no fim do mundo. [<a href="#N1">1</a>] Outros dizem dele: 'Ele também está incluído no mundo; ele também ainda não está liberto do mundo.' Eu também digo isso: 'Ele também está incluído no mundo; ele também ainda não está liberto do mundo.'</p>

<p class=Normal> (2) - (4) "Outra vez, abandonando o pensamento aplicado e sustentado, um bhikkhu entra e permanece no segundo jhana ... no terceiro jhana ... no quarto jhana. Ele é chamado um bhikkhu que, tendo chegado ao fim do mundo permanece no fim do mundo. Outros dizem dele: 'Ele também está incluído no mundo; ele também ainda não está liberto do mundo.' Eu também digo isso: 'Ele também está incluído no mundo; ele também ainda não está liberto do mundo.'</p>

<p class=Normal> (5) "Outra vez, com a completa superação das percepções da forma, com o desaparecimento das percepções do contato sensorial, sem dar atenção às percepções da diversidade, consciente de que o ‘espaço é infinito,’ ele entra e permanece na base do espaço infinito. Ele é chamado um bhikkhu que, tendo chegado ao fim do mundo permanece no fim do mundo. Outros dizem dele: 'Ele também está incluído no mundo; ele também ainda não está liberto do mundo.' Eu também digo isso: 'Ele também está incluído no mundo; ele também ainda não está liberto do mundo.'</p>

<p class=Normal> (6) - (8) "Outra vez, com a completa superação da base do espaço infinito ... com a completa superação da base da consciência infinita ... com a completa superação da base do nada, um bhikkhu entra e permanece na base da nem percepção, nem não percepção. Ele é chamado um bhikkhu que, tendo chegado ao fim do mundo permanece no fim do mundo. Outros dizem dele: 'Ele também está incluído no mundo; ele também ainda não está liberto do mundo.' Eu também digo isso: 'Ele também está incluído no mundo; ele também ainda não está liberto do mundo.'</p>

<p class=Normal> (9) "Outra vez, com a completa superação da base da nem percepção, nem não percepção, um bhikkhu entra e permanece na cessação da percepção e sensação, e tendo visto com sabedoria, as suas impurezas são destruídas. Ele é chamado um bhikkhu que, tendo chegado ao fim do mundo permanece no fim do mundo, tendo superado o apego ao mundo."</p>

