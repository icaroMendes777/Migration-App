<p class=Normal style='margin-left:.75in'> 

<p class=Normal> <a name=R1></a>Certa ocasião o Abençoado estava em Savatthi, no Bosque de Jeta, no Parque de Anathapindika. Agora naquela ocasião o rei Pasenadi havia regressado da frente de batalha, vitorioso na batalha, tendo alcançado o seu propósito. [<a href="#N1">1</a>] Então ele saiu em direção ao Parque. Ele foi até onde a estrada permitia o acesso das carruagens e depois desmontou da sua carruagem e entrou no Parque à pé. Agora, naquela ocasião muitos bhikkhus estavam caminhando para cá e para lá ao ar livre. Então, o Rei Pasenadi foi até eles e perguntou: </p>

<p class=Normal> “Veneráveis senhores, onde ele está agora, o Abençoado, um arahant, perfeitamente iluminado? Nós queremos ver o Abençoado, um arahant, perfeitamente iluminado.” </p>

<p class=Normal> “Aquela é a sua habitação, grande rei, com a porta fechada. Vá até lá em silêncio, sem pressa, entre na varanda, limpe a garganta e bata na porta. O Abençoado abrirá a porta.” </p>

<p class=Normal> Sem se apressar, o Rei Pasenadi foi em silêncio até a habitação com a porta fechada, entrou na varanda, limpou a garganta e bateu na porta. O Abençoado abriu a porta. </p>

<p class=Normal> Então, o Rei Pasenadi entrou na habitação prostrando-se com a cabeça aos pés do Abençoado, cobrindo os pés do Abençoado com beijos e acariciando-os com as mãos, pronunciando o seu nome: “Eu sou o Rei Pasenadi de Kosala, venerável senhor; Eu sou o Rei Pasenadi de Kosala, venerável senhor.” </p>

<p class=Normal> “Mas, grande rei, que razão você vê para fazer uma homenagem tão extrema para este corpo e demonstrar tamanha amizade?” [<a href="#N2">2</a>]</p>

<p class=Normal> <a name=R2></a> (1) "Pois, venerável senhor, o Abençoado pratica para o bem-estar de muitos, pela felicidade de muitos; ele estabeleceu muitas pessoas no nobre método, isto é, no caminho do bom Dhamma, no caminho do Dhamma que é benéfico. Essa é uma razão porque ofereço essa homenagem extrema ao Abençoado e demonstro tanta amizade. </p>

<p class=Normal> (2) "Outra vez, venerável senhor, o Abençoado é virtuoso, com comportamento maduro, comportamento nobre, comportamento benéfico, possuindo comportamento benéfico. Essa é outra razão porque ofereço essa homenagem extrema ao Abençoado e demonstro tanta amizade. </p>

<p class=Normal> (3) "Outra vez, venerável senhor, durante muito tempo o Abençoado tem habitado as florestas, recorrendo a moradias afastadas nas florestas e bosques. Essa é outra razão porque ofereço essa homenagem extrema ao Abençoado e demonstro tanta amizade. </p>

<p class=Normal> (4) "Outra vez, venerável senhor, o Abençoado está satisfeito com qualquer tipo de mantos, comida esmolada, moradias, e medicamentos. Essa é outra razão porque ofereço essa homenagem extrema ao Abençoado e demonstro tanta amizade. </p>

<p class=Normal> (5) "Outra vez, venerável senhor, o Abençoado é merecedor de dádivas, merecedor de hospitalidade, merecedor de oferendas, merecedor de saudações com reverência, um campo inigualável de mérito para o mundo. Essa é outra razão porque ofereço essa homenagem extrema ao Abençoado e demonstro tanta amizade. </p>

<p class=Normal> (6) "Outra vez, venerável senhor, o Abençoado ouve de acordo com a sua vontade, sem problemas ou dificuldades, conversa sobre o ascetismo que favorece a abertura do coração, isto é, falar sobre: poucos desejos, contentamento, isolamento, não ficar ligado [com outros], despertar a energia, comportamento virtuoso, concentração, sabedoria, libertação, conhecimento e visão da libertação. Essa é outra razão porque ofereço essa homenagem extrema ao Abençoado e demonstro tanta amizade. </p>

<p class=Normal> (7) "Outra vez, venerável senhor, o Abençoado obtém de acordo com a sua vontade, sem problemas ou dificuldades, os quatro jhanas que constituem a mente superior e que proporcionam uma estada prazerosa aqui e agora. Essa é outra razão porque ofereço essa homenagem extrema ao Abençoado e demonstro tanta amizade. </p>

<p class=Normal> (8) "Outra vez, venerável senhor, o Abençoado se recorda das suas muitas vidas passadas, isto é, um nascimento, dois ... cinco, dez ... cinqüenta, cem, mil, cem mil, muitos ciclos cósmicos de contração, muitos ciclos cósmicos de expansão, muitos ciclos cósmicos de contração e expansão: 'Lá eu tinha tal nome, pertencia a tal clã, tinha tal aparência. Assim era o meu alimento, assim era a minha experiência de prazer e dor, assim foi o fim da minha vida. Falecendo daquele estado eu ressurgi ali. Ali eu também tinha tal nome, pertencia a tal clã, tinha tal aparência. Assim era o meu alimento, assim era a minha experiência de prazer e dor, assim foi o fim da minha vida. Falecendo daquele estado eu ressurgi aqui.' Essa é outra razão porque ofereço essa homenagem extrema ao Abençoado e demonstro tanta amizade. </p>

<p class=Normal> (9) "Outra vez, venerável senhor, através do olho divino, que é purificado e que sobrepuja o humano, o Abençoado vê seres falecendo e renascendo e ele compreende como eles são inferiores e superiores, bonitos e feios, afortunados e desafortunados de acordo com o seu kamma: 'Esses seres – que são dotados de má conduta com o corpo, linguagem e mente, que insultam os nobres, que têm entendimento incorreto e realizam ações sob a influência do entendimento incorreto – na dissolução do corpo, após a morte, reaparecem num estado de privação, num destino infeliz, nos reinos inferiores, até mesmo no inferno. Porém, aqueles seres – que são dotados de boa conduta com o corpo, linguagem e mente, que não insultam os nobres, que têm entendimento correto e realizam ações sob a influência do entendimento correto – com a dissolução do corpo, após a morte, renascem num destino feliz, no paraíso.' Dessa forma – através do olho divino, que é purificado e que sobrepuja o humano – o Abençoado vê seres falecendo e renascendo e eu compreendi como eles são inferiores e superiores, bonitos e feios, afortunados e desafortunados de acordo com o seu kamma.  Essa é outra razão porque ofereço essa homenagem extrema ao Abençoado e demonstro tanta amizade. </p>

<p class=Normal> (10) "Outra vez, venerável senhor, o Abençoado tendo realizado por si mesmo através do conhecimento direto, aqui e agora, entrou e permanece na libertação da mente e libertação através da sabedoria que são imaculadas com a destruição de todas as impurezas  Essa é outra razão porque ofereço essa homenagem extrema ao Abençoado e demonstro tanta amizade. </p>

<p class=Normal> E agora, venerável senhor, nós partiremos. Estamos muito ocupados e temos muito que fazer.” </p>

<p class=Normal> “Agora é o momento, grande rei, faça como julgar adequado.” </p>

<p class=Normal> Então o rei Pasenadi de Kosala levantou-se do seu assento e depois de homenagear o Abençoado, mantendo-o à sua direita, partiu.
</p>


