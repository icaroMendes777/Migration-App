<p class=Normal><b>Notas:</b></p>



<p class=Normal> <a name=N1></a> [1]  Os comentários explicam o background histórico: Quando o rei Kosala, o Grande, (pai de Pasenadi), deu a sua filha em matrimônio para Bimbisara, o rei de Magadha, ele deu como presente de casamento o vilarejo de Kasi que se situava na fronteira entre os dois reinos. Anos mais tarde, quando Ajatasattu assassinou o pai Bimbisara, a sua mãe morreu de tristeza. Pasenadi então decidiu que como Ajatasattu tinha matado o pai e a mãe, o vilarejo então pertenceria ao seu pai. Ajatasattu também pensou que o vilarejo pertencia à sua mãe (falecida). Ambos, tio e sobrinho, entraram em guerra pela posse de Kasi. Pasenadi foi derrotado duas vezes por Ajatasattu, mas na terceira ocasião ele capturou Ajatasattu. Esse era o "propósito" mencionado no sutta. </p>

<p class=Normal> O SN III.15 relata que tendo capturado Ajatasattu: </p>

<p class=Normal> "O rei Pasenadi confiscou todos os elefantes, cavalaria, carruagens e infantaria do rei Ajatasattu, deixando-o livre, com nada além da sua própria vida." [<a href="#R1">Retorna</a>] </p>

