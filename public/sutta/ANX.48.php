<p class=Normal> “Bhikkhus, há essas dez coisas em relação às quais alguém que seguiu a vida santa deve refletir com freqüência. Quais dez? </p>

<p class=Normal> “’Eu entrei numa condição desprovida de castas’: alguém que seguiu a vida santa deve refletir isso com freqüência. </p>

<p class=Normal> “’A minha vida depende da generosidade dos outros’: alguém que seguiu a vida santa deve refletir isso com freqüência. </p>

<p class=Normal> “’Meu comportamento deve ser diferente [dos leigos]’: alguém que seguiu a vida santa deve refletir isso com freqüência. </p>

<p class=Normal> “’Posso encontrar algum defeito na minha virtude?’: alguém que seguiu a vida santa deve refletir isso com freqüência. </p>

<p class=Normal> “’Os meus sábios companheiros na vida santa, ao me examinarem, podem encontrar algum defeito na minha virtude?’: alguém que seguiu a vida santa deve refletir isso com freqüência. </p>

<p class=Normal> “’Eu me tornarei diferente, separado de tudo o que é para mim querido e amado’: alguém que seguiu a vida santa deve refletir isso com freqüência.  </p>

<p class=Normal> “’Eu sou o dono das minhas ações (<i>kamma</i>), herdeiro das minhas ações, nascido das minhas ações, relacionado através das minhas ações e tenho as minhas ações como árbitro. O que quer que eu faça, para o bem ou para o mal, disso me tornarei o herdeiro: alguém que seguiu a vida santa deve refletir isso com freqüência. </p>

<p class=Normal> “’No que estou me tornando na medida em que os dias e as noites voam?’: alguém que seguiu a vida santa deve refletir isso com freqüência. </p>

<p class=Normal> “’Eu me deleito com cabanas vazias?’: alguém que seguiu a vida santa deve refletir isso com freqüência. </p>

<p class=Normal> “’Eu alcancei alguma realização supra-humana, uma distinção no conhecimento e visão digna dos nobres, de modo que se for questionado pelos meus companheiros na vida santa no momento da minha morte eu não me sentirei envergonhado?’: alguém que seguiu a vida santa deve refletir isso com freqüência. </p>

<p class=Normal> “Essas são as  dez coisas em relação às quais alguém que seguiu a vida santa deve refletir com freqüência.” </p>

