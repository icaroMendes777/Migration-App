<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em Savathi.
Então o venerável Bhumija saindo do seu isolamento no final da tarde foi até o venerável Sariputta.
Ao chegar ambos se cumprimentaram. Quando a conversa cortês e amigável havia
terminado ele sentou a um lado e disse para o venerável Sariputta: <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigo
Sariputta, existem alguns brâmanes e contemplativos que ensinam sobre o kamma e
declaram que o prazer e a dor são produzidos pela própria pessoa. Existem
outros brâmanes e contemplativos que ensinam sobre o kamma e declaram que o
prazer e a dor são produzidos pelos outros. Depois existem outros brâmanes e
contemplativos que ensinam sobre o kamma e declaram que o prazer e a dor são
produzidos tanto pela própria pessoa como pelos outros. E depois existem ainda
outros brâmanes e contemplativos que ensinam sobre o kamma e declaram que o
prazer e a dor surgem ao acaso sem serem criados pela própria pessoa e tampouco
pelos outros. Neste caso, amigo Sariputta, qual é a doutrina do Abençoado? O
que ele ensina? Como poderei responder de acordo com aquilo que foi dito pelo
Abençoado, sem deturpá-lo com algo contrário aos fatos? E como deverei explicar de 
acordo com o Dhamma, de tal modo que nada que dê margem à censura possa com legitimidade 
ser deduzido da minha declaração?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O
Abençoado, meu amigo, disse que o prazer e a dor possuem origem dependente.
Dependente de que? Dependente do contato. Se alguém dissesse isso estaria falando 
o que foi dito pelo Abençoado e não o estaria deturpando com algo contrário aos fatos; 
estaria explicando de acordo com o Dhamma, de tal modo que nada que dê margem à censura 
possa com legitimidade ser deduzido da sua declaração.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“No caso
dos brâmanes e contemplativos que ensinem sobre o kamma e declarem que o prazer
e a dor são produzidos pela própria pessoa; no caso dos brâmanes e
contemplativos que ensinem sobre o kamma e declarem que o prazer e a dor são
produzidos pelos outros; no caso dos brâmanes e contemplativos que ensinem
sobre o kamma e declarem que o prazer e a dor são produzidos tanto pela própria
pessoa como pelos outros; no caso dos brâmanes e contemplativos que ensinem
sobre o kamma e declarem que o prazer e a dor surgem ao acaso sem serem criados
pela própria pessoa e tampouco pelos outros - em cada um desses casos, o prazer
e a dor estão condicionados pelo contato”.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Que os
brâmanes e contemplativos, que ensinam sobre o kamma e declaram que o prazer e
a dor são produzidos pela própria pessoa, possam ser sensíveis ao prazer e à
dor de outra forma que através do contato: isso não é possível. Que os brâmanes
e contemplativos, que ensinam sobre o kamma e declaram que o prazer e a dor são
produzidos pelos outros, possam ser sensíveis ao prazer e à dor de outra forma
que através do contato: isso não é possível. Que os brâmanes e contemplativos,
que ensinam sobre o kamma e declaram que o prazer e a dor são produzidos tanto
pela própria pessoa como pelos outros,<span style="mso-spacerun: yes"> 
</span>possam ser sensíveis ao prazer e à dor de outra forma que através do
contato: isso não é possível. Que os brâmanes e contemplativos, que ensinam
sobre o kamma e declaram que o prazer e a dor surgem ao acaso sem serem criados
pela própria pessoa e tampouco pelos outros, possam ser sensíveis ao prazer e à
dor de outra forma que através do contato: isso não é possível.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Agora,
ocorre que o venerável Ananda ouviu a conversa entre o venerável Sariputta e o venerável
Bhumija. Então ele foi até o Abençoado e ao chegar, depois de cumprimentá-lo,
sentou a um lado e relatou toda a conversa para o Abençoado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(O
Abençoado disse:) “Muito bem, Ananda. Alguém que responda da forma correta
responderia como o venerável Sariputta respondeu.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu disse,
Ananda, que o prazer e a dor possuem origem dependente. Dependente de que?
Dependente do contato. Se alguém dissesse isso estaria falando o que foi dito por 
mim e não estaria me deturpando com algo contrário aos fatos; estaria explicando 
de acordo com o Dhamma, de tal modo que nada que dê margem à censura possa com 
legitimidade ser deduzido da sua declaração. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“No caso
dos brâmanes e contemplativos que ensinam sobre o kamma e declaram que o prazer
e a dor são produzidos pela própria pessoa; no caso dos brâmanes e
contemplativos que ensinam sobre o kamma e declaram que o prazer e a dor são
produzidos pelos outros; no caso dos brâmanes e contemplativos que ensinam
sobre o kamma e declaram que o prazer e a dor são produzidos tanto pela própria
pessoa como pelos outros; no caso dos brâmanes e contemplativos que ensinam
sobre o kamma e declaram que o prazer e a dor surgem ao acaso sem serem criados
pela própria pessoa e tampouco pelos outros, em cada um desses casos, o prazer
e a dor estão condicionados pelo contato”.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Que os brâmanes e contemplativos, que ensinam sobre o kamma e declaram
que o prazer e a dor são produzidos pela própria pessoa, possam ser sensíveis
ao prazer e à dor de outra forma que através do contato: isso não é possível.
Que os brâmanes e contemplativos, que ensinam sobre o kamma e declaram que o
prazer e a dor são produzidos pelos outros, possam ser sensíveis ao prazer e à
dor de outra forma que através do contato: isso não é possível. Que os brâmanes
e contemplativos, que ensinam sobre o kamma e declaram que o prazer e a dor são
produzidos tanto pela própria pessoa como pelos outros,<span
style="mso-spacerun: yes">  </span>possam ser sensíveis ao prazer e à dor de
outra forma que através do contato: isso não é possível. Que os brâmanes e
contemplativos, que ensinam sobre o kamma e declaram que o prazer e a dor
surgem ao acaso sem serem criados pela própria pessoa e tampouco pelos outros,
possam ser sensíveis ao prazer e à dor de outra forma que através do contato:
isso não é possível.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando
existe um corpo, o prazer e a dor surgem no interior devido à volição corporal;
ou quando existe a linguagem, o prazer e a dor surgem no interior devido à
volição verbal; ou quando existe a mente, o prazer e a dor surgem no
interior<span style="mso-spacerun: yes">  </span>devido à volição mental –
tendo a ignorância como condição. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Seja com base
na própria iniciativa, a pessoa produz a formação volitiva corporal
condicionada através da qual o prazer e a dor surgem no interior; ou estimulada
por outros, a pessoa produz a formação volitiva corporal condicionada através
da qual o prazer e a dor surgem no interior. (Da mesma forma com as formações
verbais e mentais).<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“A
ignorância está compreendida nesses estados. Mas com o desaparecimento e
cessação sem deixar vestígios dessa mesma ignorância, não há o corpo
condicionado pelo qual o prazer e a dor surgem no interior. Não há<span
style="mso-spacerun: yes">  </span>linguagem ... a mente condicionada pela qual
o prazer e a dor surgem no interior. O campo não existe, o local não existe, o
fundamento não existe, a base não existe, condicionada pela qual o prazer e a
dor surgem no interior.” <o:p></o:p></span></p>


