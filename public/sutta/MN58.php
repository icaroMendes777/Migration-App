<p class=Normal>
1. Assim ouvi. Em certa ocasião o Abençoado estava  em Rajagaha, no Bambual, no Santuário dos
Esquilos. <o:p></o:p></span></p>


<p class=Normal>
2. Então o Príncipe Abhaya [<a href="#Nota1">1</a>] <a name=Texto1></a>foi
até o Nigantha Nataputta e
depois de cumprimentá-lo sentou a um lado e  o Nigantha Nataputta disse:<o:p></o:p></span></p>


<p class=Normal>
3. &quot;Venha,<span class=GramE><span style='mso-spacerun:yes'> 
</span></span>Príncipe, refute as palavras do contemplativo Gotama, e este
admirável relato a seu respeito irá se espalhar por grandes distâncias: 'A
doutrina do contemplativo Gotama – que é tão forte e poderoso – foi refutada
pelo Príncipe Abhaya!'&quot; <o:p></o:p></span></p>


<p class=Normal>
&quot;Mas como, <span class=GramE>venerável senhor, refutarei</span> a
sua doutrina?&quot; <o:p></o:p></span></p>


<p class=Normal>
&quot;Venha, Príncipe, vá até o contemplativo Gotama e chegando diga o
seguinte: 'Venerável senhor, o Tathagata diria palavras que são antipáticas e
desagradáveis para outras <span class=GramE>pessoas?</span>' Se o contemplativo
Gotama, perguntado dessa forma, responder 'O Tathagata diria palavras que são
antipáticas e desagradáveis para outras pessoas,' então você diria, 'Então,
venerável senhor,<span style='mso-spacerun:yes'>  </span>qual é a diferença
entre você e as pessoas comuns? Pois as pessoas comuns também dizem palavras
que são antipáticas e desagradáveis para outras pessoas.’ Porém se o
contemplativo Gotama, perguntado dessa forma, responder, 'O Tathagata não diria
palavras que são antipáticas e desagradáveis para outras pessoas,' então você
diria, 'Então como, venerável senhor, você falou acerca de Devadatta que
&quot;Devadatta está destinado aos estados de privação, Devadatta está indo em
direção ao inferno, Devadatta irá permanecer [no inferno] por um éon, Devadatta
é incorrigível?<span class=GramE>&quot; </span>Pois Devadatta ficou zangado e
insatisfeito com essas suas palavras.' Quando essa questão com duas pontas for
colocada para o contemplativo Gotama, ele não terá como engoli-la ou cuspi-la.
Tal como se uma pedaço de ferro com duas pontas ficasse entalado na garganta de
um homem, ele não conseguiria engoli-lo ou cuspi-lo. Da mesma forma, quando
essa questão com duas pontas for colocada para o contemplativo Gotama, ele não
será capaz de engoli-la ou cuspi-la.&quot; <o:p></o:p></span></p>


<p class=Normal>
4. &quot;Sim, venerável senhor,&quot; respondeu o Príncipe Abhaya.
Então, ele levantou do seu assento, e depois de homenagear o Nigantha
Nataputta, mantendo-o à sua direita, partiu e se dirigiu até o Abençoado. Depois de cumprimentá-lo, ele sentou a um
lado, olhou para o Sol e pensou, &quot;Hoje é muito tarde para refutar a
doutrina do Abençoado. Amanhã na minha própria casa eu irei refutar a doutrina
do Abençoado.&quot; Então ele disse para o Abençoado:
&quot;Venerável senhor, que o Abençoado, juntamente com outros três, aceite o meu convite
 para a refeição de amanhã.&quot;<span style='mso-spacerun:yes'> 
</span>O Abençoado concordou em silêncio. <o:p></o:p></span></p>


<p class=Normal>
5. Então, sabendo que o Abençoado havia concordado, o Príncipe Abhaya
levantou do seu assento, e depois de homenagear o Abençoado, mantendo-o à sua
direita, partiu. Então, quando havia terminado a noite, ao amanhecer, o
Abençoado se vestiu e carregando a sua tigela e o manto externo foi
até a casa do Príncipe Abhaya, sentando num assento que havia sido preparado.
Então, com as próprias mãos, o Príncipe Abhaya serviu e satisfez o Abençoado
com vários tipos de boa comida. Em seguida, quando o Abençoado havia terminado
de comer e retirado <span class=GramE>a</span> mão da sua tigela, o Príncipe
Abhaya sentou a um lado, num assento mais baixo, e disse para o Abençoado: <o:p></o:p></span></p>


<p class=Normal>
6. &quot;Venerável senhor, um Tathagata diria palavras que são antipáticas
e desagradáveis para outras pessoas?<span class=GramE>&quot;</span> <o:p></o:p></span></p>

<p class=Normal>
&quot;Príncipe, não existe uma resposta única para essa pergunta.&quot;
<o:p></o:p></span></p>

<p class=Normal>
&quot;Então, venerável senhor, neste caso os Niganthas perderam.&quot; <o:p></o:p></span></p>

<p class=Normal>
&quot;Mas Príncipe, porque você diz isto: 'Então, venerável senhor,
neste caso os Niganthas perderam'?&quot; [<a href="#N2">2</a><span class=GramE>]</span><o:p></o:p></span></p>

<p class=Normal><a name=R2></a> O Príncipe Abhaya então relatou ao Abençoado toda a
conversa com o Nigantha Nataputta.<span style='mso-spacerun:yes'>  </span><o:p></o:p></span></p>


<p class=Normal>7.</span></span><span lang=PT-BR style='mso-ansi-language:PT-BR'>Agora,
naquela ocasião um bebê menino estava deitado no colo do Príncipe com o rosto
para cima. Então o Abençoado disse ao Príncipe, &quot;O que você <span
class=GramE>pensa,</span> Príncipe: se enquanto você ou a ama-seca não
estivessem prestando atenção, essa criança colocasse um graveto ou uma pedra na
própria boca, o que você faria?&quot; <o:p></o:p></span></p>

<p class=Normal>
&quot;Eu o tiraria, venerável senhor. Se eu não conseguisse tirar com
facilidade, então segurando a sua cabeça com a minha mão esquerda e curvando um
dedo da mão direita, eu o tiraria, mesmo se com isso ele se machucasse. Por que
isso? Porque eu tenho compaixão pelo jovem bebê.&quot; <o:p></o:p></span></p>


<p class=Normal>
8. &quot;Da mesma forma, Príncipe: no caso de palavras que o Tathagata
sabe que não correspondem aos fatos, não são verdadeiras, não são benéficas e
que também são antipáticas e desagradáveis para outras pessoas: essas palavras,
ele não as diz. No caso de palavras que o Tathagata sabe que correspondem aos
fatos, são verdadeiras, não são benéficas e que também são antipáticas e
desagradáveis para outras pessoas: essas palavras, ele não as diz. No caso de
palavras que o Tathagata sabe que correspondem aos fatos, são verdadeiras, são
benéficas, porém são antipáticas e desagradáveis para outras pessoas: o
Tathagata tem a noção do momento mais apropriado para dizê-<span class=GramE>las.</span>[<a
href="#N3">3</a>]<span style='mso-spacerun:yes'>  </span><a name=R3></a>No caso
de palavras que o Tathagata sabe que não correspondem aos fatos, não são
verdadeiras, não são benéficas, porém são simpáticas e agradáveis para outras
pessoas: essas palavras ele não as diz. No caso de palavras que o Tathagata
sabe que correspondem aos fatos, são verdadeiras, não são benéficas, porém são
simpáticas e agradáveis para outras pessoas: essas palavras ele
não as diz. No caso de palavras que o Tathagata sabe que correspondem aos
fatos, são verdadeiras e benéficas e que também são simpáticas e agradáveis
para outras pessoas: o Tathagata tem a noção do momento mais apropriado para
dizê-las. Por que isso? Porque o Tathagata tem compaixão pelos seres
vivos.&quot; <o:p></o:p></span></p>


<p class=Normal> 9. &quot;Venerável senhor, quando nobres sábios,
brâmanes sábios, chefes de família sábios e contemplativos sábios, tendo
formulado uma questão<span style='mso-spacerun:yes'>  </span>procuram o
Abençoado e lhe perguntam, o pensamento já está na mente do Abençoado -<span
style='mso-spacerun:yes'>  </span>'Se aqueles que me procuram<span
style='mso-spacerun:yes'>  </span>perguntarem isto, eu – perguntado dessa forma
– responderei assim' – ou o Tathagata encontra a resposta no momento?&quot; <o:p></o:p></span></p>


<p class=Normal>
10. &quot;Nesse caso, Príncipe, eu lhe farei uma contra pergunta.
Responda como quiser. O que você pensa: você conhece bem as partes de uma
carruagem?&quot; <o:p></o:p></span></p>

<p class=Normal>
&quot;Sim, venerável senhor, eu conheço bem as partes de uma carruagem.&quot; <o:p></o:p></span></p>

<p class=Normal>
&quot;E o que você <span class=GramE>pensa,</span> Príncipe? Quando as
pessoas o procuram e perguntam: 'Qual é o nome desta parte da carruagem?' O
pensamento já está na sua mente<span style='mso-spacerun:yes'>  </span>-<span
style='mso-spacerun:yes'>  </span>'Se aqueles que me procuram<span
style='mso-spacerun:yes'>  </span>perguntarem isto, eu – perguntado dessa forma
– responderei assim' – ou você encontra a resposta no momento?&quot; <o:p></o:p></span></p>

<p class=Normal>
&quot;Venerável senhor, eu sou famoso por conhecer bem todas as partes
de uma carruagem. Todas as partes de uma carruagem me são bem familiares. Eu
encontro <span class=GramE>a</span> resposta no momento.&quot; <o:p></o:p></span></p>


<p class=Normal>
11. &quot;Da mesma forma, Príncipe, quando nobres sábios, brâmanes
sábios, chefes de família sábios e contemplativos sábios,<span class=GramE><span
style='mso-spacerun:yes'>  </span></span>tendo formulado uma questão procuram o
Abençoado e lhe perguntam, ele encontra a resposta na hora. Por que isso?
Porque o elemento natureza das coisas foi completamente penetrado pelo
Tathagata, através dessa completa penetração, ele encontra as respostas no
momento.&quot; [<a href="#N4">4</a>]<o:p></o:p></span></p>


<p class=Normal><a name=R4></a> 12. Quando isso foi
dito, o Príncipe Abhaya disse ao Abençoado: &quot;Magnífico, venerável senhor!
Magnífico, venerável senhor! O Abençoado  esclareceu o Dhamma de várias formas, como se tivesse
colocado em pé o que estava de cabeça para baixo, revelasse o que estava
escondido, mostrasse o caminho para alguém que estivesse perdido ou segurasse
uma lâmpada no escuro para aqueles que possuem visão pudessem ver as formas.
Nós buscamos refúgio no Abençoado, no Dhamma e na Sangha dos bhikkhus. Que a partir de hoje o
Abençoado se recorde de mim como um discípulo leigo que nele buscou refúgio
para o resto da sua vida.&quot; <o:p></o:p></span></p>

