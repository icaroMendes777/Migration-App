<p class=Normal><b>Introdução </b> (por Thanissaro Bhikkhu)</span></p>

<p class=Normal> 
Neste discurso, o Buda mostra os fatores que decidem o que deve ser
dito e o que não deve ser dito. Os principais fatores são três: se uma
afirmação é verdadeira ou não, se é benéfica ou não e, se é agradável para os
outros ou não. O próprio Buda afirmaria somente aquelas coisas que são
verdadeiras e benéficas, e tinha uma noção do momento em que coisas agradáveis
e desagradáveis deviam ser ditas. Observe que a possibilidade que uma afirmação
sendo falsa possa<span class=GramE> no entanto</span> ser benéfica não é
considerada.<o:p></o:p></span></p>


<p class=Normal>
Este discurso também mostra, na prática, o ensinamento do Buda<span
class=GramE><span style='mso-spacerun:yes'>  </span></span>acerca dos quatro
tipos de perguntas e como estas devem ser respondidas.<span
style='mso-spacerun:yes'>  </span>O Príncipe lhe faz duas perguntas e em ambos
os casos ele responde primeiro com uma contra pergunta, antes de dar uma
resposta analítica à primeira pergunta e uma resposta categórica à segunda.
Cada contra pergunta tem um duplo propósito: dar ao Príncipe um ponto de
referência conhecido para entender a resposta que virá em seguida e também,
para lhe dar a oportunidade de expressar o seu próprio entendimento e bons
motivos. Isto também dá ao Príncipe a oportunidade de salvar sua honra após ter
sido frustrado no seu desejo de superar o Buda na argumentação. O Comentário
observa que o Príncipe havia colocado o seu filho pequeno no colo como um
truque barato de alguém que irá participar de um debate: se o Buda o colocasse
em uma posição desconfortável durante o debate, o Príncipe beliscaria o seu
filho provocando o seu choro e dessa forma efetivamente pararia o debate. O
Buda, no entanto, usa a presença da criança para remover qualquer noção de que
haverá um debate e também para fazer uma observação eficaz. Tomando a idéia do
Nigantha Nataputta de um objeto perigoso entalado na garganta, ele o aplica à
criança, e então faz a observação de que, ao contrário dos Niganthas – que não
veriam problema em deixar alguém com um objeto perigoso entalado na garganta –
o desejo do Buda é de remover tais objetos, por compaixão. Dessa forma<span
class=GramE><span style='mso-spacerun:yes'>  </span></span>ele traz o Príncipe
para o seu lado, convertendo um oponente em potencial em um discípulo. <o:p></o:p></span></p>


<p class=Normal>
Assim este discurso não é somente<span class=GramE><span
style='mso-spacerun:yes'>  </span></span><i style='mso-bidi-font-style:normal'>sobre</i>
linguagem correta mas também mostra a linguagem correta sendo empregada. <o:p></o:p></span></p>

