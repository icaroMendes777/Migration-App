<p class=Normal>“Há um método, bhikkhus, através do qual um bhikkhu, além da fé, além da preferência, além da tradição, além da razão e além das idéias, [<a href="#N1">1</a>]<a name="R1"> </a>é capaz de declarar o conhecimento supremo assim: ‘O nascimento foi destruído, a vida santa foi vivida, o que deveria ser feito foi feito, não há mais vir a ser a nenhum estado’?” </p>
<p class=Normal> “Venerável senhor, os nossos ensinamentos têm o Abençoado como origem, como guia e como refúgio. Seria bom se o Abençoado pudesse explicar o significado dessas palavras. Tendo ouvido do Abençoado, os bhikkhus o recordarão.” </p>
<p class=Normal> “Então, bhikkhus, ouçam e prestem muita atenção àquilo que eu vou dizer.” – “Sim, venerável senhor,” os bhikkhus responderam. O Abençoado disse o seguinte: </p>
<p class=Normal> “Há um método através do qual um bhikkhu, além da  fé, além da preferência, além da tradição, além da razão e além das idéias, é capaz de declarar o conhecimento supremo assim: ‘O nascimento foi destruído, a vida santa foi vivida, o que deveria ser feito foi feito, não há mais vir a ser a nenhum estado.’ E qual é esse método? Aqui, bhikkhus, tendo visto uma forma com o olho, se houver cobiça, raiva ou delusão internamente, um bhikkhu compreende: ‘Há raiva, cobiça ou delusão internamente’; ou, se não há cobiça, raiva ou delusão internamente, ele compreende: ‘Não há cobiça, raiva ou delusão internamente.’ Em sendo assim, essas coisas devem ser compreendidas através da fé ou através da preferência, ou através da tradição, ou através da razão, ou através das idéias?” </p>

<p class=Normal> “Não, venerável senhor.” </p>

<p class=Normal> “Essas coisas não devem ser compreendidas vendo-as com sabedoria?” </p>

<p class=Normal> “Sim, venerável senhor.” </p>

<p class=Normal> “Esse, bhikkhus, é o método através do qual um bhikkhu é capaz de declarar o conhecimento supremo assim: ‘O nascimento foi destruído, a vida santa foi vivida, o que deveria ser feito foi feito, não há mais vir a ser a nenhum estado.’ </p>

<p class=Normal> “Além disso, bhikkhus, tendo ouvido um som com o ouvido ... tendo conscientizado um objeto mental com a mente, se houver cobiça, raiva ou delusão internamente, um bhikkhu compreende: ‘Há raiva, cobiça ou delusão internamente’; ou, se não há cobiça, raiva ou delusão internamente, ele compreende: ‘Não há cobiça, raiva ou delusão internamente.’ Em sendo assim, essas coisas devem ser compreendidas através da  fé ou através da preferência, ou através da tradição, ou através da razão, ou através das idéias?” </p>

<p class=Normal> “Não, venerável senhor.” </p>

<p class=Normal> “Essas coisas não devem ser compreendidas vendo-as com sabedoria?” </p>

<p class=Normal> “Sim, venerável senhor.” </p>

<p class=Normal> “Esse, bhikkhus, é o método através do qual um bhikkhu é capaz de declarar o conhecimento supremo assim: ‘O nascimento foi destruído, a vida santa foi vivida, o que deveria ser feito foi feito, não há mais vir a ser a nenhum estado.’” </p>

