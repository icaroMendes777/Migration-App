<p class=Normal><b>Nota de Thanissaro Bhikkhu:</b></p>

<p class=Normal><a name=N1></a><b>1.</b> Esta afirmação gerou muita controvérsia ao longo
dos séculos. O Comentário afirma que “mente” neste caso se refere a <i><a
href="../glossario.php#bhavanga">bhavanga-citta</a>,</i> mas essa afirmação
levanta mais questões do que respostas. Não há referência a <i>bhavanga-citta</i>
ou fluxo mental em nenhum dos suttas (isto aparece pela primeira vez no
Patthana do Abhidhamma); e como os comentários comparam <i>bhavanga-citta</i>
com o sono profundo, por que isso é chamado de luminoso? E por que a compreensão da sua luminosidade seria um pré-requisito para o desenvolvimento
da mente? E além disso, se “mente” neste sutta significa bhavanga-citta, o que significaria
desenvolver <i>bhavanga-citta</i>? </p>

<p class=Normal>Outra interpretação iguala a luminosidade da mente com a “consciência sem
características,” descrita como “luminosa” no <a href="MN49.php#N14">MN 49</a>
e no <a href="DN11.php">DN 11</a>, mas esta interpretação também é problemática.
De acordo com o MN 49, essa consciência não compartilha nada do mundo
descritível, nem mesmo a “Totalidade do Todo,” então como poderia ela estar
poluída? E, como ela não pode ser alcançada até que o objetivo da prática seja
realizado, por que a compreensão da sua luminosidade seria um pré-requisito
para o desenvolvimento da mente? E uma vez mais, se “mente” neste caso
significa a consciência sem características, como pode este sutta falar sobre
o seu desenvolvimento? </p>

<p class=Normal>Uma abordagem mais razoável para compreender a afirmação deste sutta pode
ser derivada tomando-a dentro do seu contexto: a mente luminosa é a mente que o
meditador está tentando desenvolver. Compreender a sua luminosidade significa
compreender que as máculas,como a cobiça, raiva e delusão, não são
intrínsecas à natureza da mente, não são uma parte necessária da consciência.
Sem essa compreensão seria impossível praticar. Com essa compreensão, no
entanto, a pessoa poderá se esforçar para eliminar as máculas existentes,
deixando a mente em um estado que o <a href="MN24.php">MN 24</a> denomina
“purificação da Mente.” Isso corresponde ao nível luminoso de concentração
descrito no simile do quarto jhana: “<span lang=PT-BR style='mso-ansi-language:
PT-BR'>E além disso, com o completo desaparecimento da felicidade, um bhikkhu entra e permanece no quarto jhana, que possui nem felicidade nem sofrimento, com a atenção plena e a equanimidade purificadas. Ele permanece permeando o corpo com a mente pura e luminosa, de forma que não exista nada em todo o corpo que não esteja permeado pela mente pura e luminosa.” A partir desse
estado é possível desenvolver o insight que não somente elimina as máculas
existentes mas também desenraiza qualquer possibilidade de que elas possam surgir
novamente. Somente nos estágios da iluminação que vêm depois desse insight será
alcançada<span style="mso-spacerun: yes">  </span>a “consciência sem
características”. [<a href="#R1">Retorna</a>]</span></p>

