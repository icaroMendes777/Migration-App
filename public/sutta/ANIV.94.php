<p class=Normal>“Bhikkhus, há<span lang=PT-BR style='mso-ansi-language:PT-BR'>
quatro tipos de pessoas que podem ser encontradas no mundo</span>. Quais
quatro?</p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Há um tipo de pessoa que </span>alcançou
a tranqüilidade da mente, mas não o insight dos fenômenos através da sabedoria
superior. Depois há um tipo de pessoa que alcançou o insight dos fenômenos
através da sabedoria superior, mas não a tranqüilidade da mente. Depois há
um tipo de pessoa que nem alcançou a tranqüilidade da mente e tampouco o insight
dos fenômenos através da sabedoria superior. E depois há um tipo de pessoa
que alcançou ambos, a tranqüilidade da mente e o insight dos fenômenos através
da sabedoria superior. </p>

<p class=Normal>“A pessoa que alcançou a tranqüilidade da mente, mas não o insight dos
fenômenos através da sabedoria superior, deveria se acercar daquele que
alcançou o insight dos fenômenos através da sabedoria superior e
perguntar: ‘Como devem ser consideradas as formações? Como elas devem ser
investigadas? Como elas devem ser vistas através do insight?’ O outro irá
responder de acordo com aquilo que ele viu e experienciou: ‘As formações devem
ser consideradas desta forma. As formações devem ser investigadas desta forma. As
formações devem ser vistas através do insight desta forma.’ Então, com o tempo,
ele [o primeiro] irá se tornar alguém que alcançou ambos, a tranqüilidade da
mente e o insight dos fenômenos através da sabedoria superior. </p>

<p class=Normal>“Quanto à pessoa que alcançou o insight dos fenômenos através da sabedoria
superior, mas não a tranqüilidade da mente, ela deveria se acercar daquele
que alcançou a tranqüilidade da mente e perguntar, ‘Como a mente deve ser
estabilizada? Como deve ser feito para tranquilizar a mente? Como a mente deve
ser unificada? Como a mente deve ser concentrada?’ O outro irá responder de
acordo com aquilo que ele viu e experienciou: ‘A mente deve ser estabilizada
desta forma. A mente deve ser tranquilizada desta forma. A mente deve ser
unificada desta forma. A mente deve ser concentrada desta forma.’ Então, com o
tempo, ele [o primeiro] irá se tornar alguém que alcançou ambos, a tranqüilidade
da mente e o insight dos fenômenos através da sabedoria superior. </p>

<p class=Normal>“Quanto à pessoa que nem alcançou a tranqüilidade da mente e nem o insight
dos fenômenos através da sabedoria superior, ela deveria se acercar
daquele que alcançou ambos, a tranqüilidade da mente e o insight dos fenômenos
através da sabedoria superior e perguntar, ‘Como a mente deve ser
estabilizada? Como deve ser feito para tranquilizar a mente? Como a mente deve
ser unificada? Como a mente deve ser concentrada? Como devem ser consideradas
as formações? Como elas devem ser investigadas? Como elas devem ser vistas
através do insight?’ O outro irá responder de acordo com aquilo que ele viu e
experienciou: ‘A mente deve ser estabilizada desta forma. A mente deve ser tranquilizada
desta forma. A mente deve ser unificada desta forma. A mente deve ser
concentrada desta forma. As formações devem ser consideradas desta forma. As
formações devem ser investigadas desta forma. As formações devem ser vistas
através do insight desta forma.’ Então, com o tempo, ele [o primeiro] irá se
tornar alguém que alcançou ambos, a tranqüilidade da mente e o insight dos
fenômenos através da sabedoria superior. </p>

<p class=Normal>“Quanto à pessoa que alcançou ambos, a tranqüilidade da mente e o insight
dos fenômenos através da sabedoria superior, a sua tarefa é fazer esforço
em estabelecer (‘afinar’) essas mesmas qualidades hábeis num nível superior
para dar fim às impurezas mentais. </p>

<p class=Normal>“Esses são os <span lang=PT-BR style='mso-ansi-language:PT-BR'>quatro tipos
de pessoas que podem ser encontradas no mundo.” </span></p>

