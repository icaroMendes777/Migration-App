<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] “Com a incapacidade de fazer o mal”, <i>ahimsaya,</i> significa ter
compaixão ou estar no estágio preliminar da compaixão, isto é o acesso ao primeiro
jhana produzido pela meditação de compaixão. “Que sente amor bondade por todos
os seres”, <i>metta, </i>significa aquele que desenvolve amor bondade, ou que
está no estágio preliminar do amor bondade. “Para ele não existe<span
style="mso-spacerun: yes">  </span>inimizade com ninguém”, alguém para quem o amor
bondade e compaixão é uma parte do seu caráter. Por causa da mente cheia de
raiva, alguém poderia nutrir inimizade até mesmo por um arahant que não tenha desenvolvido
a meditação do amor bondade e compaixão. Mas ninguém poderia nutrir inimizade
por aquele que é dotado da libertação da mente através do amor bondade e
compaixão, tão poderosa é a meditação das moradas divinas. [<a href="#R1">Retorna</a>]<o:p></o:p></span></p>



