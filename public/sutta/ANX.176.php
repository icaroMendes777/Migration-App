<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ouvi que em
certa ocasião o Abençoado estava próximo a Pava no manguezal do ferreiro Cunda.
Então o ferreiro Cunda </span>foi até o Abençoado e depois de cumprimentá-lo sentou
a um lado e o Abençoado disse<span lang=PT-BR style='mso-ansi-language:PT-BR'>:
&quot;Cunda, quais rituais de purificação você aprova?&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Os brâmanes das
terras do Ocidente, senhor - aqueles que carregam potes com água, usam
grinaldas de plantas aquáticas, cultuam o fogo e purificam com a água: eles declararam
rituais de purificação que eu aprovo.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;E que tipos de
rituais de purificação eles declararam, esses brâmanes das terras do Ocidente
que carregam potes com água, usam grinaldas de plantas aquáticas, cultuam o
fogo e purificam com a água?&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;É o caso em que os
brâmanes das terras do Ocidente … fazem com que os seus discípulos pratiquem da
seguinte forma: 'Venha, agora, meu bom homem: Levante-se na hora apropriada da
sua cama e toque a terra. Se você não tocar a terra, toque o estrume de vaca
úmido. Se você não tocar o estrume de vaca úmido, toque a grama verde. Se você
não tocar a grama verde, cultue o fogo. Se você não cultuar o fogo, homenageie
o sol com as mãos postas. Se você não homenagear o sol com as mãos postas,
entre na água três vezes ao cair da noite'. Esses são os rituais de purificação
declarados pelos brâmanes das terras do Ocidente … que eu aprovo.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Cunda, os rituais de
purificação declarados pelos brâmanes das terras do Ocidente … são uma coisa, a
purificação na disciplina dos nobres é algo totalmente diferente.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Mas como é a
purificação na disciplina dos nobres, venerável senhor? Seria bom se o
Abençoado me ensinasse como é a purificação na disciplina dos nobres.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Então nesse caso,
Cunda, ouça e preste </span>muita atenção àquilo que eu vou dizer<span
lang=PT-BR style='mso-ansi-language:PT-BR'>.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;</span>Sim, venerável
senhor<span lang=PT-BR style='mso-ansi-language:PT-BR'>. O ferreiro Cunda
respondeu. <o:p></o:p></span></p>

<p class=Normal><a name=BM10unskillful><span lang=PT-BR style='mso-ansi-language:PT-BR'>O
Abençoado</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'> disse:
&quot;Há três formas através das quais alguém se torna impuro pelas ações
corporais, quatro formas através das quais alguém se torna impuro pelas ações
verbais e três formas pelas quais alguém se torna impuro pelas ações mentais. <o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ação Corporal Inábil e
Prejudicial<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;E como alguém se
torna impuro de três formas pela ação corporal? É o caso em que alguém tira a
vida de outros seres, um homicida, sanguinário, dedicado a golpes e violência,
demonstrando nenhuma piedade com os seres vivos. Ele toma o que não é dado, ele
toma, como se fosse um ladrão, os bens e propriedades de outros em um vilarejo
ou na floresta. Ele se comporta de forma imprópria em relação aos prazeres
dos sentidos; ele se envolve sexualmente com quem está sob a proteção da mãe, do
pai, dos irmãos, das irmãs, dos parentes, que possuem esposo, protegidas pela
lei ou mesmo com quem esteja coroada de flores por um outro homem. Assim é como
alguém se torna impuro de três formas pela ação corporal. <o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ação Verbal Inábil e
Prejudicial<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;E como alguém se
torna impuro de quatro formas pela ação verbal? É o caso em que alguém emprega
linguagem mentirosa; tendo sido chamado para uma corte, uma reunião, um encontro
com seus parentes, com a sua corporação, com a família real, se assim for
questionado como testemunha: 'Então, bom homem, diga o que você sabe,' se ele
não souber, dirá, 'Eu sei'; se ele souber, dirá, 'Eu não sei'; se ele não viu,
dirá, 'Eu vi;' se ele viu, dirá, 'Eu não vi'; em plena consciência ele conta
mentiras em seu próprio benefício, pelo benefício de outros ou para obter algum
benefício mundano insignificante. Ele emprega linguagem maliciosa; o que ouviu
aqui ele conta ali para separar aquelas pessoas destas, ou o que ele ouviu lá
conta aqui para separar estas pessoas daquelas; assim ele separa aquelas
pessoas que estão unidas, ele cria divisões, ama a discórdia, se delicia com a
discórdia, desfruta da discórdia, diz coisas que criam a discórdia. Ele emprega
linguagem grosseira; ele emprega palavras que são grosseiras, duras, que magoam
os outros, que ofendem os outros, próximas da raiva e que não favorecem a
concentração. Ele emprega a linguagem frívola; ele fala fora de hora, diz o que
não é fato, diz o que é inútil, diz aquilo que é contrário ao Dhamma e a
Disciplina; nas horas inadequadas ele diz palavras que são inúteis,
irracionais, imoderadas e que não trazem benefício. Assim é como alguém se
torna impuro de quatro formas pela ação verbal.<span style="mso-spacerun:
yes">    </span><o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ação Mental Inábil e
Prejudicial<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;E como alguém se
torna impuro de três formas pela ação mental? É o caso em que alguém é
cobiçoso; ele cobiça os bens e propriedade dos outros, pensando, 'Ah, que
aquilo que pertence aos outros seja meu!' Ou a sua mente possui má vontade e as
suas intenções estão plenas de raiva: 'Que esses seres sejam mortos e
assassinados, que eles cessem, faleçam ou sejam aniquilados!' Ou ele tem
entendimento incorreto, vê as coisas de forma distorcida: 'Não existe nada que
é dado, nada que é oferecido, nada que é sacrificado; não existe fruto ou
resultado de ações boas ou más; não existe este mundo nem outro mundo; não
existe mãe, nem pai; nenhum ser que renasça espontaneamente; não existem no
mundo brâmanes nem contemplativos bons e virtuosos que, após terem conhecido e
compreendido diretamente por eles mesmos, proclamam este mundo e o próximo.'
Assim é como alguém se torna impuro de três formas pela ação mental. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Essas, Cunda, são os
dez tipos de ações inábeis. Quando alguém pratica esses dez tipos de ações
inábeis, então mesmo se ele se levantar da cama na hora apropriada e tocar a
terra, ainda será impuro. Se ele não tocar a terra, ainda será impuro. Se ele
tocar estrume de vaca úmido, ainda será impuro. Se ele não tocar estrume de
vaca úmido, ainda será impura. Se ele tocar a grama verde ... se ele não tocar
a grama verde … se ele cultuar o fogo … se ele não cultuar o fogo … se ele
homenagear o sol com as mãos postas … se ele não homenagear o sol com as mãos
postas … se ele entrar na água três vezes ao anoitecer … se ele não entrar na
água três vezes ao anoitecer, ainda será impuro. Por que isso? Porque esses dez
tipos de ações inábeis são impuras e causam impureza. Além disso, como
resultado de praticar esses dez tipos de ações inábeis, o [renascimento no]
inferno é declarado, o [renascimento no] ventre animal é declarado, o
[renascimento no] reino dos fantasmas é declarado - isso ou qualquer outro
destino ruim. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;<a name=BM10skillful>Agora,
Cunda</a>, há três formas através das quais alguém se torna puro pelas ações
corporais, quatro formas através das quais alguém se torna puro pelas ações
verbais e três formas pelas quais alguém se torna puro pelas ações mentais. <o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ação Corporal Hábil e
Benéfica<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;E como alguém se
torna puro de três formas pela ação corporal? É o caso em que alguém, abandonando
tirar a vida de outros seres, se abstém de tirar a vida de outros seres; ele
permanece com a sua vara e arma postas de lado, bondoso e gentil, compassivo
com todos os seres vivos. Abandonando tomar o que não seja dado, ele se abstém
de tomar o que não é dado; ele não toma, como se fosse um ladrão, os bens e
propriedades de outros num vilarejo ou na floresta. Abandonando a conduta
imprópria com relação aos prazeres dos sentidos, ele se abstém da conduta imprópria
com relação aos prazeres dos sentidos; ele não se envolve sexualmente com quem está
sob a proteção da mãe, do pai, dos irmãos, das irmãs, dos parentes, que possuem
esposo, protegidas pela lei ou mesmo com quem esteja coroada de flores por um
outro homem. Assim é como alguém se torna puro de três formas pela ação
corporal.<o:p></o:p></span></p>

<p class=Normal><b><a name=speech><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ação Verbal
</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>Hábil e Benéfica<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot; E como alguém se
torna puro de quatro formas pela ação verbal? É o caso em que alguém,
abandonando a linguagem mentirosa, se abstém da linguagem mentirosa; tendo sido
chamado para uma corte, uma reunião, um encontro com seus parentes, com a sua
corporação, com a família real, se assim for questionado como testemunha:
'Então, bom homem, diga o que você sabe,' se ele não souber, dirá, 'Eu não
sei'; se ele souber, dirá, 'Eu sei'; se ele não viu, dirá, 'Eu não vi'; se ele
viu, dirá, 'Eu vi'. Assim com plena consciência ele não conta mentiras em seu
próprio benefício, pelo benefício de outros ou para obter algum benefício
mundano insignificante. Abandonando a linguagem maliciosa, ele se abstém da linguagem
maliciosa; o que ouviu aqui ele não conta ali para separar aquelas pessoas
destas, ou, o que ouviu lá ele não conta aqui para separar estas pessoas
daquelas; assim ele reconcilia aquelas pessoas que estão divididas, promove a
amizade, ele ama a concórdia, se delicia com a concórdia, desfruta da
concórdia, diz coisas que criam a concórdia. Abandonando a linguagem grosseira,
ele se abstém da linguagem grosseira. Ele diz palavras que são gentis, que
agradam aos ouvidos, carinhosas, que penetram o coração, que são corteses,
desejadas por muitos e que agradam a muitos. Abandonando a linguagem frívola,
ele se abstém da linguagem frívola. Ele fala na hora certa, diz o que é fato,
aquilo que é bom, fala de acordo com o Dhamma e a Disciplina; nas horas adequadas
ele diz palavras que são úteis, racionais, moderadas e que trazem benefício.
Assim é como alguém se torna puro de quatro formas pela ação verbal.<o:p></o:p></span></p>

<p class=Normal><b><a name=mind><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ação Mental </span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>Hábil e Benéfica<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot; E como alguém se
torna puro de três formas pela ação mental? É o caso em que alguém não é
cobiçoso. Ele não cobiça as posses dos outros, pensando, 'Ah, que aquilo que
pertence aos outros seja meu!' A sua mente não possui má vontade e as suas
intenções estão isentas de raiva: 'Que esses seres possam estar livres da
inimizade, aflição e ansiedade! Que eles vivam felizes!’ Ele tem entendimento
correto e não vê as coisas de forma distorcida: ‘Existe aquilo que é dado e o
que é oferecido e o que é sacrificado; existe fruto e resultado de boas e más
ações; existe este mundo e o outro mundo; existe a mãe e o pai; existem seres
que renascem espontaneamente; existem no mundo brâmanes e contemplativos bons e
virtuosos que, após terem conhecido e compreendido diretamente por eles mesmos,
proclamam este mundo e o próximo.’ Assim é como alguém se torna puro de três
formas pela ação mental. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Essas, Cunda, são os
dez tipos de ações hábeis. Quando alguém pratica esses dez tipos de ações
hábeis, então mesmo se ele se levantar da cama na hora apropriada e tocar a
terra, ainda será puro. Se ele não tocar a terra, ainda será puro. Se ele tocar
estrume de vaca úmido, ainda será puro. Se ele não tocar estrume de vaca úmido,
ele ainda será puro. Se ele tocar a grama verde ... se ele não tocar a grama
verde … se ele cultuar o fogo … se ele não cultuar o fogo … se ele homenagear o
sol com as mãos postas … se ele não homenagear o sol com as mãos postas … se
ele entrar na água três vezes ao anoitecer … se ele não entrar na água três
vezes ao anoitecer, ele ainda será puro. Por que isso? Porque esses dez tipos de
ações hábeis são puras e causam pureza. Além disso, como resultado de praticar
esses dez tipos de ações hábeis, os [renascimento entre] devas são declarados,
os [renascimento entre] seres humanos são declarados - isso ou qualquer outro
destino bom.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isso foi dito, o
ferreiro Cunda disse para o Abençoado: &quot;Magnífico, venerável senhor!
Magnífico, venerável senhor! Como se colocasse em pé o que estava de cabeça
para baixo, revelasse o que estava escondido, mostrasse o caminho para alguém
que estivesse perdido, ou levasse uma lâmpada no escuro para que aqueles que
possuem visão conseguissem ver as formas, da mesma forma o Abençoado - através
de muitas linhas de raciocínio - esclareceu o Dhamma. Eu busco refúgio no
Abençoado, no Dhamma e na Sangha dos bhikkhus. Que o Abençoado se recorde de
mim como um discípulo leigo que buscou refúgio nele, deste dia em diante, por
toda a vida&quot;. <o:p></o:p></span></p>

