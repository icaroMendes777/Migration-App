<p class=Normal> “Bhikkhus, esta é a precursora e antecessora do nascer do sol, isto é, a alvorada. Do mesmo modo, bhikkhus, para um bhikkhu esta é a precursora e antecessora do surgimento dos sete fatores da iluminação, isto é, a atenção com sabedoria. Quando um bhikkhu é consumado na atenção com sabedoria, é de se esperar que ele desenvolva e cultive os sete fatores da iluminação. </p>
 
<p class=Normal> “E como um bhikkhu, que é consumado na atenção com sabedoria, desenvolve e cultiva os sete fatores da iluminação? Nesse caso, bhikkhus, um bhikkhu desenvolve o fator da iluminação da atenção plena, que tem como base o afastamento, desapego e cessação, que amadurece no abandono ... ele desenvolve o fator da iluminação da equanimidade, que tem como base o afastamento, desapego e cessação, que amadurece no abandono. É dessa forma, bhikkhus, que um bhikkhu, que é consumado na atenção com sabedoria, desenvolve e cultiva os sete fatores da iluminação.”</p>


