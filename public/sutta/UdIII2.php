<p class=Normal style='margin-left:.75in'>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Certa ocasião, o Abençoado estava em Savatthi, no Bosque de<span
style='mso-spacerun:yes'>  </span>Jeta, no Parque de Anathapindika. Nessa
ocasião, o<span style='mso-spacerun:yes'>  </span>Ven. Nanda – meio irmão do
Abençoado, filho da sua tia materna – disse para um grande número de bhikkhus, “Eu
não aprecio viver a vida santa, meus amigos. Eu não consigo tolerar a vida
santa. Desistindo do treinamento, eu retornarei para a vida comum.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então,
um certo bhikkhu foi até o Abençoado e depois de cumprimentá-lo sentou a um
lado e disse: “Senhor, o Ven. Nanda – o meio irmão do Abençoado, filho da sua tia
materna – contou para um grande grupo de bhikkhus, ‘Eu não aprecio viver a vida
santa, meus amigos. Eu não consigo tolerar a vida santa. Desistindo do
treinamento, eu retornarei para a vida comum.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então
o Abençoado disse para esse bhikkhu, “Venha, bhikkhu em meu nome chame Nanda,
dizendo-lhe, ‘O Mestre o chama, meu amigo.’” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim, senhor,” o bhikkhu respondeu e, 
tendo ido até o Ven. Nanda, ao chegar ele
disse, “O Mestre o chama, meu amigo” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
amigo”, respondeu o Ven. Nanda. Então ele foi até o Abençoado e depois de
cumprimentá-lo sentou a um lado e o Abençoado disse: “É verdade, Nanda, que
você disse para um grande grupo de bhikkhus, ‘Eu não aprecio viver a vida
santa, meus amigos. Eu não consigo tolerar a vida santa. Desistindo do
treinamento, eu retornarei para a vida comum’?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
senhor.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas
por que, Nanda, você não aprecia viver a vida santa?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Senhor,
quando eu estava deixando a minha casa, uma moça Sakya – que causava inveja a
todos – olhou para mim, com o seu cabelo parcialmente penteado, e disse, ‘Volte
depressa, senhor.’ Relembrando isso, eu não aprecio viver a vida santa. Eu não
consigo tolerar a vida santa. Desistindo do treinamento eu retornarei para a
vida comum.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então,
tomando o Ven. Nanda pelo braço – como um homem forte flexiona o seu braço
estendido ou estende seu braço flexionado – o Abençoado desapareceu do Bosque
de Jeta e reapareceu entre os devas do paraíso de Tavatimsa. Agora naquela
ocasião cerca de 500 ninfas com pés rosados vieram esperar por Sakka, o
senhor dos devas. E o Abençoado disse ao Ven. Nanda, “Nanda, você vê essas 500
ninfas com os pés rosados?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
senhor.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O
que você pensa, Nanda: Qual é a mais bela, com melhor aparência, mais
encantadora – a moça Sakya, que causa inveja a todos, ou essas 500 ninfas com
os pés rosados?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Senhor,
comparada com essas 500 ninfas com pés rosados a moça Sakya, que causa inveja
a todos, é como um macaco cujas orelhas e nariz foram cortados e cauterizados.
Ela não pode ser levada em conta. Ela não é nem uma fração. Não há comparação.
As 500 ninfas com pés rosados são mais bonitas, com melhor aparência, mais
encantadoras.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então
alegre-se, Nanda. Alegre-se! Eu sou a sua garantia para conseguir 500 ninfas
com pés rosados.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se
o Abençoado é minha garantia para conseguir 500 ninfas com os pés rosados, eu
apreciarei viver a vida santa sob o Abençoado.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então,
tomando o Ven. Nanda pelo braço - como um homem forte flexiona o seu braço
estendido ou estende seu braço flexionado – o Abençoado desapareceu dentre os
devas do paraíso de Tavatimsa e reapareceu no Bosque de Jeta. Os bhikkhus
ouviram, “Dizem que o Ven. Nanda – o meio irmão do Abençoado, filho da sua tia
materna – está vivendo a vida santa por conta de ninfas. Eles dizem que o
Abençoado é a sua garantia para obter 500 ninfas com os pés rosados.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então,
os bhikkhus que eram amigos do Ven. Nanda dirigiam-se a ele como se
ele fosse um empregado e um negociante: “Nosso amigo Nanda, eles diziam, é um
empregado. Nosso amigo Nanda, eles diziam, é um negociante. Ele vive a vida
santa por conta de ninfas. O Abençoado é a sua garantia para obter 500 ninfas
com os pés rosados.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então
o Ven. Nanda – envergonhado, repelido e desgostoso, porque os bhikkhus que eram
seus amigos estavam se dirigindo a ele como se ele fosse um empregado e um
negociante – se afastou, permanecendo só, isolado, diligente, ardente e
decidido, em pouco tempo, ele entrou e permaneceu no objetivo supremo da vida
santa pelo qual membros de um clã deixam a vida em família pela vida santa,
tendo conhecido e realizado por si mesmo no aqui e agora. Ele soube: “O
nascimento foi destruído, a vida santa foi vivida, o que deveria ser feito foi
feito, não há mais vir a ser a nenhum estado.” E assim o Ven. Nanda tornou-se
mais um dos Arahants. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, na última vigília da
noite, uma certa devata com belíssima aparência que iluminou todo o Bosque de
Jeta, se aproximou do Abençoado. Tendo se aproximado ela homenageou o Abençoado
e ficando em pé a um lado a devata disse: “Senhor, o Ven. Nanda - o meio irmão do
Abençoado, filho da sua tia materna – compreendendo por ele mesmo com conhecimento direto, aqui e agora, 
entrou e permanece na libertação da mente e na libertação através da sabedoria, que são imaculadas, 
com a destruição de todas as impurezas.” E no
Abençoado surgiu o conhecimento: “Nanda, compreendendo por ele mesmo com conhecimento direto, aqui e agora, 
entrou e permanece na libertação da mente e na libertação através da sabedoria, que são imaculadas, 
com a destruição de todas as impurezas.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, quando a noite havia
terminado, o Ven. Nanda foi até o Abençoado e depois de cumprimentá-lo sentou a
um lado e disse: “Senhor, com respeito ao Abençoado ser a minha garantia para a
obtenção de 500 ninfas com os pés rosados, eu por meio disto libero o
Abençoado dessa promessa.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Nanda, tendo compreendido
a sua mente com a minha mente, eu compreendi que ‘Nanda, compreendendo por ele mesmo com conhecimento direto, aqui e agora, 
entrou e permanece na libertação da mente e na libertação através da sabedoria, que são imaculadas, 
com a destruição de todas as impurezas.’ E uma devata me informou que ‘O Ven. Nanda, compreendendo por ele mesmo com conhecimento direto, aqui e agora, 
entrou e permanece na libertação da mente e na libertação através da sabedoria, que são imaculadas, 
com a destruição de todas as impurezas.’ Quando a sua mente, através da ausência de apego, se libertou das
impurezas, eu por meio disso fiquei liberado daquela promessa.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, dando-se conta do
significado disso, o Abençoado nessa ocasião exclamou: <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:PT-BR'>Aquele
que<br>
&nbsp;&nbsp;&nbsp; atravessou o atoleiro,<br>
&nbsp;&nbsp;&nbsp; esmagou o espinho dos prazeres sensuais,<br>
&nbsp;&nbsp;&nbsp; alcançou o fim da delusão,<br>
é um bhikkhu que não é perturbado<br>
pelo êxtase e pela dor. <o:p></o:p></span></p>

