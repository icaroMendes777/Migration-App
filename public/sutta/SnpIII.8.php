<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“A vida
neste mundo é incerta e imprevisível. A vida aqui é difícil, breve, e atada ao
sofrimento. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Um ser, uma
vez que tenha nascido, irá morrer, não há como escapar disso. Quando a velhice
chega, ou alguma outra causa, a morte é inevitável. Esse é o curso natural para
um ser vivo. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando os
frutos estão maduros há o constante perigo que eles caiam. Da mesma forma, para
aqueles que nasceram e que estão sujeitos à morte, sempre há o medo da morte. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Tal como
os potes feitos por um oleiro sempre acabam se quebrando, assim também é a vida
dos mortais. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Os jovens
e os velhos, quer sejam tolos ou sábios, todos são detidos pelo poder da morte,
todos enfim acabam na morte. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqueles
tomados pela morte e que passam a um outro mundo, um pai não pode proteger o
seu filho, nem os parentes a alguém próximo. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Veja:
enquanto os parentes observam, chorando e gemendo, um a um todos mortais são
levados iguais aos bois ao matadouro.<span style='mso-spacerun:yes'>  </span><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto o
envelhecimento e a morte são endêmicos ao mundo. Mas os sábios não se
angustiam, tendo compreendido a natureza do mundo. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Você não
tem como saber o caminho através do qual eles vieram ou para onde irão. Então
não tem sentido lamentar-se em vão. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se algum
benefício fosse obtido da lamentação, os sábios lamentariam. Apenas um tolo
fere a si mesmo. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“No entanto
por meio do choro e da tristeza a mente não se acalma, apenas mais sofrimento e
dor são produzidos. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O luto
emagrece e empalidece, apenas há prejuízo para si mesmo. Não é possível manter
vivos aqueles que morreram. O pesar é inútil. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Por não
abandonar a tristeza um ser simplesmente sofre mais. O pesar faz dele um escravo
da tristeza. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Veja os
seres enfrentando a morte, experimentando os resultados das suas ações. As
pessoas se aterrorizam quando percebem estarem sob o poder da morte. <o:p></o:p></span></p>



<p class=Normal><a name=Sn588></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“O que as pessoas esperam acontecer é sempre diferente daquilo que
realmente acontece. Disso surge um grande desapontamento. Assim é como funciona
o mundo. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Alguém
pode viver por cem anos, ou até mais, mas no final, ele é separado do seu
círculo de entes queridos e abre mão da sua vida. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto
podemos ouvir e aprender do nobre, ao vê-lo abandonar o pesar. Quando ele vê
alguém que viveu a sua vida e faleceu, ele diz, ‘Ele não mais será visto por
mim.’ <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando uma
casa está em chamas o fogo é extinto com água. Da mesma forma um sábio, hábil,
estudado, com autoconfiança, extingue a tristeza assim que ela surge. Igual ao
vento soprando um tufo de algodão. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aquele que
busca a felicidade deve remover a flecha que ele mesmo se meteu: a flecha das
angústias, dos desejos, do desespero.<span style='mso-spacerun:yes'>   </span><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Com a
flecha removida, desapegado, com a mente em paz, tendo superado a tristeza,
livre da tristeza, ele terá realizado nibbana.<o:p></o:p></span></p>




