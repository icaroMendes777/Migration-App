<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] O Punna deste sutta não é o mesmo Punna Mantaniputta do <a
href="MN24.php">MN 24</a>. Ele era de uma família de comerciantes que residiam
na cidade portuária de Supparaka em Sunaparanta (Maharashtra na atualidade). Em
uma viagem de negócios a Savathi ele ouviu um discurso do Buda e decidiu
abandonar a vida em família para tornar-se um bhikkhu. [</span><a href="#R1"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>Retorna</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[2] MA explica esta orientação como sendo um breve ensinamento das
quatro nobres verdades. Deleite <i style='mso-bidi-font-style:normal'>(nandi) </i>é
um dos aspectos de desejo. Através do surgimento do deleite com relação ao olho
e as formas surge o sofrimento dos cinco agregados. Assim nesta primeira parte
da instrução o Buda ensina o ciclo de existências por meio das duas primeiras
verdades - o sofrimento e a sua origem - tal como elas ocorrem através dos seis
sentidos. Na segunda parte ele ensina o fim do ciclo por meio das duas últimas
verdades - cessação e o caminho - expressadas como o abandono do deleite nos
seis sentidos e os seus objetos. [</span><a href="#R2"><span lang=PT-BR
style='mso-ansi-language:PT-BR'>Retorna</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=N3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[3] Isto é, ele morreu. Como o Buda ainda se refere a Punna como o
membro de um clã <i style='mso-bidi-font-style:normal'>(kulaputta), </i>ele
deve ter morrido após um breve período depois de<span style="mso-spacerun:
yes">  </span>retornar a Sunaparanta. Os textos não possuem registro de como
ele morreu.[<a href="#R3">Retorna</a>]<i style='mso-bidi-font-style:normal'> </i><o:p></o:p></span></p>



