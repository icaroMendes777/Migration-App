<p class=Normal> "Bhikkhus, (1) quando não há a atenção plena e a plena consciência, naquele deficiente na atenção plena e na plena consciência, a vergonha e o temor de cometer transgressões está carente de sua condição. (2) Quando não há a vergonha e o temor de cometer transgressões, naquele deficiente na vergonha e o temor de cometer transgressões, (3) a contenção das faculdades dos sentidos está carente de sua condição. Quando não há a contenção das faculdades dos sentidos, naquele deficiente na contenção das faculdades dos sentidos, (4) o comportamento virtuoso está carente de sua condição. Quando não há o comportamento virtuoso, naquele deficiente no comportamento virtuoso, (5) a concentração correta está carente de sua condição. Quando não há a concentração correta, naquele deficiente na concentração correta, (6) o conhecimento e visão de como as coisas na verdade são está carente de sua condição. Quando não há o conhecimento e visão de como as coisas na verdade são, naquele deficiente no conhecimento e visão de como as coisas na verdade são, (7) o desencantamento e o desapego estão carentes de sua condição. Quando não há o desencantamento e o desapego, naquele deficiente no desencantamento e no desapego, (8) o conhecimento e visão da libertação estão carentes de sua condição. </p>

<p class=Normal>"Suponham uma árvore desprovida de galhos e folhas, os seus brotos não irão amadurecer, nem a sua casca, nem o seu alburno, nem o seu cerne; assim também, quando não há a atenção plena e a plena consciência, naquele deficiente na atenção plena e na plena consciência, a vergonha e o temor de cometer transgressões está carente de sua condição. Quando não há a vergonha e o temor de cometer transgressões ... o conhecimento e visão da libertação estão carentes de sua condição. </p>

<p class=Normal>"Bhikkhus, (1) quando há a atenção plena e a plena consciência, naquele que possui a atenção plena e a plena consciência, a vergonha e o temor de cometer transgressões possui a sua condição. (2) Quando há a vergonha e o temor de cometer transgressões, naquele que possui a vergonha e o temor de cometer transgressões, (3) a contenção das faculdades dos sentidos possui a sua condição. Quando há a contenção das faculdades dos sentidos, naquele que possui a contenção das faculdades dos sentidos, (4) o comportamento virtuoso possui a sua condição. Quando há o comportamento virtuoso, naquele que possui o comportamento virtuoso, (5) a concentração correta possui a sua condição. Quando há a concentração correta, naquele que possui a concentração correta, (6) o conhecimento e visão de como as coisas na verdade são possui a sua condição. Quando há o conhecimento e visão de como as coisas na verdade são, naquele que possui o conhecimento e visão de como as coisas na verdade são, (7) o desencantamento e o desapego possuem a sua condição. Quando há o desencantamento e o desapego, naquele que possui o desencantamento e o desapego, (8) o conhecimento e visão da libertação possuem a sua condição. </p>

<p class=Normal>"Suponham uma árvore provida de galhos e folhas, os seus brotos irão amadurecer, também a sua casca, também o seu alburno, também o seu cerne; assim também, quando há a atenção plena e a plena consciência, naquele que possui a atenção plena e a plena consciência, a vergonha e o temor de cometer transgressões possui a sua condição. Quando há a vergonha e o temor de cometer transgressões ... o conhecimento e visão da libertação possuem a sua condição."
</p>

