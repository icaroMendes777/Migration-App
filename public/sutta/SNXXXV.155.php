<p class=Normal> Então, um certo bhikkhu foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse:</p>

<p class=Normal> “Venerável senhor, dizem, ‘um orador do Dhamma, um orador do Dhamma.’ De que forma, venerável senhor, alguém é um orador do Dhamma?”</p>

<p class=Normal> “Bhikkhu, se um bhikkhu ensina o Dhamma tendo como propósito o desencantamento em relação ao olho, pelo seu desaparecimento e cessação, ele pode ser chamado um bhikkhu que é um orador do Dhamma. Se, através do desencantamento com o olho, através do seu desaparecimento e cessação, ele estiver libertado através do desapego, ele pode ser chamado um bhikkhu que realizou Nibbana nesta mesma vida.</p>

<p class=Normal> “Bhikkhu, se alguém ensina o Dhamma tendo como propósito o desencantamento em relação ao ouvido ... à mente, pelo seu desaparecimento e cessação, ele pode ser chamado um bhikkhu que é um orador do Dhamma. Se, através do desencantamento com o ouvido ... a mente, através do seu desaparecimento e cessação, ele estiver libertado através do desapego, ele pode ser chamado um bhikkhu que realizou Nibbana nesta mesma vida." </p>

