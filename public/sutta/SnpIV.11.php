<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Notas:<o:p></o:p></span></b></p>



<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] Julgamentos ou avaliações das coisas motivadas pelo desejo por elas,
ou pelas idéias ou opiniões que elas são desejáveis ou não. [<a href="#R1">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[2] O “surgimento” do atrativo e o “desaparecimento” do não atrativo são
julgados como “bom”. O “surgimento” do não atrativo e o “desaparecimento” do atrativo
são julgados como “ruim”. [<a href="#R2">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[3] Isto é do atrativo e não atrativo. [<a href="#R3">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[4] Ele não é insano nem perturbado mentalmente. [<a href="#R4">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N5></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[5] Ele alcançou o estado de cessação da percepção e sensação (<i>sañña-vedayita
nirodha</i>) ou as realizações imateriais (<i>arupajjhana</i>).
No primeiro caso a percepção cessa por completo, mas no ultimo a percepção de
um objeto imaterial ainda está presente. [<a href="#R5">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[6] De acordo com o comentário o que resta depois dessas quatro negações
é o estado daquele que alcançou o jhana mais elevado e está no processo de
alcançar a primeira realização imaterial. Isto
responde à pergunta “Para quem desaparece a (percepção da) materialidade (forma)?” E
quanto ao “prazer e a dor” foi mencionado antes que estes têm como causa o
contato, em outras palavras, a percepção das formas, desta forma a segunda
questão também está respondida. [<a href="#R6">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N7></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[7] “Proliferações mentais” (<i>papañca-sankha</i>): A tendência da
mente de criar distinções e diferenciações até mesmo nas experiências mais
banais no presente, dando dessa forma origem a idéias que resultam em conflito.
Tal como indicado no <a href="SnpIV.14.php">Snp IV.14</a>, a causa dessas
classificações é a percepção, “Eu sou o pensador”. Veja também a nota 1 desse
discurso e o <a href="MN18.php">MN 18</a>. [<a href="#R7">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N8></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[8] Neste caso a palavra “espírito” (<i>yakkha</i>) é equivalente a
“ser” ou “pessoa”. [<a href="#R8">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N9></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[9] Uma interpretação alternativa desta sentença poderia ser: “Alguns
dos sábios não declaram as realizações imateriais como o estado mais sublime,
como a purificação de um ser?” [<a href="#R9">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N10></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[10] Em outras palavras, o sábio compreende que ambos grupos mencionados
antes recorrem ao seu conhecimento para mensurar o objetivo, sem compreender a
dependência ainda latente no seu conhecimento. Os sábios do primeiro grupo
confundem a experiência da nem percepção, nem não percepção, como sendo o
objetivo, e assim eles ainda dependem desse estado de concentração. Os sábios
do segundo grupo, pelo fato de reivindicarem serem expertos, mostram que ainda
há uma presunção latente na sua suposta experiência de iluminação, e dessa
forma não estão totalmente libertados do apego. Ambos grupos ainda sustentam o
conceito de um “espírito” que é purificado na realização da purificação. Uma
vez que essas dependências sejam compreendidas, ele se liberta das disputas e
dos estados de ser/existir e não ser/existir. É desse modo que o conhecimento é
um meio para atingir o objetivo, mas o objetivo em si não é definido em termos
de conhecimentos. [<a href="#R10">Retorna</a>]<o:p></o:p></span></p>


