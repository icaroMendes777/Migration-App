<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Do que
surgem brigas e disputas, lamentações e tristeza, junto com egoísmo, presunção
e arrogância, junto com difamação? De onde surgem essas várias coisas? Por
favor me diga.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Daquilo
que é querido, das preferências é que surgem as brigas e disputas, lamentações
e tristeza, junto com egoísmo, presunção e arrogância, junto com difamação. As
brigas e disputas estão ligadas com o egoísmo, e a difamação nasce das
disputas.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Qual é a
causa das coisas queridas, das preferências no mundo, junto com as cobiças que
predominam no mundo? E qual é a causa para as esperanças e realizações com
respeito à vida futura da pessoa?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Desejos
são a causa daquilo que é querido, das preferências no mundo, e também das
cobiças que predominam no mundo. Esses são as causas das<span
style='mso-spacerun:yes'>  </span>esperanças e realizações com respeito à vida
futura da pessoa.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Agora qual
é a causa para o desejo no mundo? Qual é a causa para o surgimento dos
julgamentos[<a href="#N1">1</a>]; <a name=R1></a>da raiva, mentiras, dúvidas e
todos os demais estados semelhantes que foram descritos pelo Abençoado?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aquilo que
no mundo é chamado “atrativo” e “não atrativo”: com base nisso surge o desejo.
Tendo visto o surgimento e desaparecimento das formas no mundo alguém faz os
seus julgamentos no mundo. [<a href="#N2">2</a>] <a name=R2></a>A raiva,
mentiras e dúvidas, esses estados apenas surgem devido à existência dessa
dualidade. [<a href="#N3">3</a>] <a name=R3></a>Aquele que tem dúvida deveria
treinar no caminho do conhecimento para compreender esses estados.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Qual é a
causa para considerar as coisas como atrativas ou não atrativas? O que estando
ausente faz com que esses estados não estejam presentes? Qual é o significado
de surgimento e desaparecimento? Diga me qual é a sua causa.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O contato
é a causa para o atrativo e não atrativo. Quando o contato está ausente, esses
estados não estão presentes. A idéia de surgimento e desaparecimento é
originada disso, eu digo.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Qual é a
causa do contato? Do que surge tanto apego? Com a ausência do que não há posse?
Com o desaparecimento do que o contato não é experimentado?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Condicionado
pela mentalidade-materialidade (nome e forma) surge o contato. O apego tem a sua causa na
querença. Sem a presença desta não há posse. Com o desaparecimento das formas o
contato não é experimentado.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Para quem
cessam as formas? Como cessam o prazer e a dor? Diga-me como cessam. Meu
coração anseia saber como cessam.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Há um
estado no qual as formas cessam de exisitir, um estado no qual não há a
percepção comum nem a percepção anormal[<a href="#N4">4</a>]; <a name=R4></a>nesse
estado não há percepção ou a percepção das formas está suspensa [<a href="#N5">5</a>]
<a name=R5></a>– assim as formas cessam. [<a href="#N6">6</a>] <a name=R6></a>A
percepção deveras é a causa de toda proliferação no mundo.”[<a href="#N7">7</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R7></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Aquilo que perguntamos, você respondeu. Agora perguntamos outra
questão. Por favor nos diga. Alguns dos sábios não declaram a purificação do
espírito [<a href="#N8">8</a>] <a name=R8></a>como sendo o estado mais sublime
a ser alcançado? E outros não dizem que algo distinto é o mais sublime?”[<a
href="#N9">9</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R9></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Alguns dos sábios declaram que a purificação do espírito é a mais
sublime. Mas em oposição a eles alguns ensinam uma doutrina de aniquilação.
Esses sabichões declaram ser essa a libertação final sem restar nenhum apego.
Sabendo que esses teorizantes se baseiam apenas em meras opiniões para afirmar
isso, um sábio investiga aquilo do qual eles dependem.[<a href="#N10">10</a>] <a
name=R10></a>Tendo compreendido e estando libertado (das teorias) ele não
disputa com ninguém. Ele não experimenta a existência e a não existência: ele
está iluminado.”<o:p></o:p></span></p>



