<p class=Normal> Certa ocasião, o venerável Sariputta e o venerável Maha Kotthita estavam em Benares no Parque do Gamo, em Isipatana. Então, o venerável Maha Kotthita, saindo do seu isolamento no final da tarde foi até o venerável Sariputta e ao chegar ambos se cumprimentaram. Quando a conversa cortês e amigável havia terminado ele sentou a um lado e disse: “Sariputta, meu amigo, quais coisas um bhikkhu estudado deveria observar com atenção com sabedoria?” </p>

<p class=Normal> “Um bhikkhu estudado, meu amigo Kotthita, deveria observar com atenção com sabedoria os cinco agregados influenciados pelo apego como impermanentes, sofrimento, uma enfermidade, um câncer, uma flecha, dolorosos, uma aflição, estranhos, uma dissolução, um vazio, não-eu. Quais cinco? A forma como um agregado influenciado pelo apego, a sensação ... a percepção ... as formações volitivas ... a consciência como um agregado influenciado pelo apego. Um bhikkhu estudado deveria observar com atenção com sabedoria esses cinco agregados influenciados pelo apego como impermanentes, sofrimento, uma enfermidade, um câncer, uma flecha, dolorosos, uma aflição, estranhos, uma dissolução, um vazio, não-eu. Pois, é possível que, um bhikkhu estudado, que observe com atenção com sabedoria esses cinco agregados como impermanentes ... não-eu, possa alcançar o fruto de ‘entrar na correnteza’”. </p>

<p class=Normal> “Então, quais coisas deveria um bhikkhu, que ‘entrou na correnteza’, observar com atenção com sabedoria?” </p>

<p class=Normal> “Um bhikkhu, que ‘entrou na correnteza,’ deveria observar com atenção com sabedoria os cinco agregados influenciados pelo apego como impermanentes, sofrimento, uma enfermidade, um câncer, uma flecha, dolorosos, uma aflição, estranhos, uma dissolução, um vazio, não-eu. Pois, é possível que um bhikkhu, que ‘entrou na correnteza,’ que observe com atenção com sabedoria esses cinco agregados como impermanentes ... não-eu, possa alcançar o fruto de ‘retornar apenas uma vez’”. </p>

<p class=Normal> “Então quais coisas deveria um bhikkhu, que ‘retorna apenas uma vez’, observar com atenção com sabedoria?” </p>

<p class=Normal> “Um bhikkhu, que ‘retorna apenas uma vez’, deveria observar com atenção com sabedoria os cinco agregados influenciados pelo apego como impermanentes, sofrimento, uma enfermidade, um câncer, uma flecha, dolorosos, uma aflição, estranhos, uma dissolução, um vazio, não-eu. Pois, é possível que um bhikkhu, que ‘retorna apenas uma vez,’ que observe com atenção com sabedoria esses cinco agregados como impermanentes ... não-eu, possa alcançar o fruto de ‘não retorno’”. </p>

<p class=Normal> “Então quais coisas deveria um bhikkhu, que ‘não retorna’, observar com atenção com sabedoria?” </p>

<p class=Normal> “Um bhikkhu, que ‘não retorna’, deveria observar com atenção com sabedoria os cinco agregados influenciados pelo apego como impermanentes, sofrimento, uma enfermidade, um câncer, uma flecha, dolorosos, uma aflição, estranhos, uma dissolução, um vazio, não-eu. Pois, é possível que um bhikkhu, que ‘não retorna,’que observe com atenção com sabedoria esses cinco agregados como impermanentes ... não-eu, possa alcançar o fruto de ‘arahant’”. </p>

<p class=Normal> “Então quais coisas um arahant deveria observar com atenção com sabedoria?” </p>

<p class=Normal> “Um arahant deveria observar com atenção com sabedoria os cinco agregados influenciados pelo apego como impermanentes, sofrimento, uma enfermidade, um câncer, uma flecha, dolorosos, uma aflição, estranhos, uma dissolução, um vazio, não-eu. Apesar de não existir nada mais a ser feito e nada a acrescentar àquilo que já foi feito por um arahant, ainda assim, quando essas coisas são desenvolvidas e cultivadas, elas conduzem a um estado prazeroso no aqui e agora, à atenção plena e à completa compreensão. </p>


