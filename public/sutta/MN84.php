<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. </span>Assim
ouvi. Em certa ocasião o<span lang=PT-BR style='mso-ansi-language:PT-BR'>
venerável Maha Kaccana </span>estava em<span lang=PT-BR style='mso-ansi-language:
PT-BR'> Madhura no Bosque de Gunda.[<a href="#N1">1</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2. O Rei Avantiputta de Madhura ouviu: “O contemplativo Kaccana </span>está
em<span lang=PT-BR style='mso-ansi-language:PT-BR'> Madhura no Bosque de Gunda.
E acerca desse mestre Kaccana existe essa boa reputação: ‘Ele é sábio, tem
discernimento, sagaz, culto, articulado e perspicaz; ele é um ancião e ele é um
arahant. É bom poder encontrar um arahant.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. Então o
Rei Avantiputta de Madhura ordenou que um grande número de carruagens reais
fossem preparadas e montando numa carruagem real, ele saiu de Madhura com toda
a pompa da realeza para ir ver o venerável Maha Kaccana. Ele foi até onde a
estrada permitia o acesso das carruagens e depois desmontou da sua carruagem e
foi a pé até onde se encontrava o venerável Maha Kaccana e ambos se
cumprimentaram. Q</span>uando a conversa cortês e amigável havia terminado<span
lang=PT-BR style='mso-ansi-language:PT-BR'>, ele sentou a um lado e disse:<o:p></o:p></span></p>



<p class=Normal><a name=T4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>4. “Mestre Kaccana, os brâmanes dizem o seguinte: ‘A casta dos Brâmanes
é a casta superior, outras castas são inferiores; a casta dos Brâmanes tem a
tez clara, as outras castas têm a tez escura; os Brâmanes são purificados, os
não Brâmanes não são; os Brâmanes são os verdadeiros filhos de Brahma, nascidos
da boca dele, nascidos de Brahma, criados por Brahma, herdeiros de Brahma.’ O
que o Mestre Kaccana diz disso?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. “Isso é
apenas um dito no mundo, grande rei, que ‘A casta dos Brâmanes é a casta
superior ... herdeiros de Brahma.’ E há uma maneira através da qual pode ser
compreendido que essa afirmação dos brâmanes é apenas um dito no mundo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O que você
pensa, grande rei? Se um nobre prospera com posses, grãos, prata ou ouro,
haverá nobres que irão se despertar antes dele e se deitar depois dele, que
estarão ávidos por serví-lo, que buscarão agradá-lo e dirigir-se-ão a ele com
doçura, e que também haverá brâmanes, comerciantes e trabalhadores que farão o
mesmo?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Haverá,
Mestre Kaccana.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O que você
pensa, grande rei? Se um brâmane prospera com posses, grãos, prata ou ouro,
haverá brâmanes que irão se despertar antes dele e se deitar depois dele, que
estarão ávidos por serví-lo, que buscarão agradá-lo e dirigir-se-ão a ele com
doçura, e que também haverá comerciantes, trabalhadores e nobres que farão o
mesmo?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Haverá,
Mestre Kaccana.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O que você
pensa, grande rei? Se um comerciante prospera com posses, grãos, prata ou ouro,
haverá comerciantes que irão se despertar antes dele e se deitar depois dele,
que estarão ávidos por serví-lo, que buscarão agradá-lo e dirigir-se-ão a ele
com doçura, e que também haverá trabalhadores, nobres e brâmanes que farão o
mesmo?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Haverá,
Mestre Kaccana.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O que você
pensa, grande rei? Se um trabalhador prospera com posses, grãos, prata ou ouro,
haverá trabalhadores que irão se despertar antes dele e se deitar depois dele,
que estarão ávidos por serví-lo, que buscarão agradá-lo e dirigir-se-ão a ele
com doçura, e que também haverá nobres, brâmanes e comerciantes que farão o
mesmo?”[<a href="#N2">2</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Haverá, Mestre Kaccana.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O que você
pensa, grande rei? Em sendo assim, então essas quatro castas são todas a mesma
coisa, ou não são, ou qual o seu parecer nesse caso?” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Com
certeza, em sendo assim, Mestre Kaccana, então essas quatro castas são todas a
mesma coisa: não há nenhuma diferença entre elas que eu possa ver.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Essa é uma
maneira, grande rei, através através da qual pode ser compreendido que essa
afirmação dos brâmanes é apenas um dito no mundo.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. “O que
você pensa, grande rei? Suponha que um nobre mate seres vivos, tome aquilo que
não é dado, se comporte de modo impróprio com relação aos prazeres sensuais,
fale mentiras, fale com malícia, fale de forma grosseira, fale frivolidades,
seja cobiçoso, tenha má vontade na mente e tenha entendimento incorreto. Com a
dissolução do corpo, após a morte, é bem provável que ele renasça num estado de
privação, num destino infeliz, nos reinos inferiores, até mesmo no <span
style='mso-bidi-font-weight:bold'>inferno</span>, ou não, ou qual o seu parecer
nesse caso?” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se um
nobre for assim, Mestre Kaccana, [é bem provável que] ele renasça<span
style="mso-spacerun: yes">  </span>num estado de privação, num destino infeliz,
nos reinos inferiores, até mesmo no <span style='mso-bidi-font-weight:bold'>inferno</span>.
Esse é o meu parecer nesse caso e assim é como ouvi dos arahants.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O que você
pensa, grande rei? Suponha que um brâmane ... um comerciante ... um trabalhador
mate seres vivos ... e tenha entendimento incorreto. Com a dissolução do corpo,
após a morte, é bem provável que ele renasça num estado de privação, num
destino infeliz, nos reinos inferiores, até mesmo no <span style='mso-bidi-font-weight:
bold'>inferno</span>, ou não, ou qual o seu parecer nesse caso?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se um
brâmane ... um comerciante ... um trabalhador for assim, Mestre Kaccana, [é bem
provável que] ele renasça<span style="mso-spacerun: yes">  </span>num estado de
privação, num destino infeliz, nos reinos inferiores, até mesmo no <span
style='mso-bidi-font-weight:bold'>inferno</span>. Esse é o meu parecer nesse
caso e assim é como ouvi dos arahants.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O que você
pensa, grande rei? Em sendo assim, então essas quatro castas são todas a mesma
coisa, ou não são, ou qual o seu parecer nesse caso?” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Com
certeza, em sendo assim, Mestre Kaccana, então essas quatro castas são todas a
mesma coisa: não há nenhuma diferença entre elas que eu possa ver.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Essa
também é uma maneira, grande rei, através através da qual pode ser compreendido
que essa afirmação dos brâmanes é apenas um dito no mundo.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. “O que
você pensa, grande rei? Suponha que um nobre se abstém de matar seres vivos,
não tome aquilo que não é dado, não se comporte de modo impróprio com relação
aos prazeres sensuais, não fale mentiras, não fale com malícia, não fale de
forma grosseira, não fale frivolidades, não seja cobiçoso, não tenha má vontade
na mente e tenha entendimento correto. Com a dissolução do corpo, após a morte,
é bem provável que ele renasça num destino feliz, até mesmo no paraíso, ou não,
ou qual o seu parecer nesse caso?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se um
nobre for assim, Mestre Kaccana, [é bem provável que] ele renasça num destino
feliz, até mesmo no paraíso. Esse é o meu parecer nesse caso e assim é como
ouvi dos arahants.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O que você
pensa, grande rei? Suponha que um brâmane ... um comerciante ... um trabalhador
se abstém de matar seres vivos ... e tenha entendimento correto. Com a
dissolução do corpo, após a morte, é bem provável que ele renasça num destino
feliz, até mesmo no paraíso, ou não, ou qual o seu parecer nesse caso?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se um
brâmane ... um comerciante ... um trabalhador fosse assim, Mestre Kaccana, [é
bem provável que] ele renasça num destino feliz, até mesmo no paraíso. Esse é o
meu parecer nesse caso e assim é como ouvi dos arahants.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O que você
pensa, grande rei? Em sendo assim, então essas quatro castas são todas a mesma
coisa, ou não são, ou qual o seu parecer nesse caso?” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Com
certeza, em sendo assim, Mestre Kaccana, então essas quatro castas são todas a
mesma coisa: não há nenhuma diferença entre elas que eu possa ver.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Essa
também é uma maneira, grande rei, através da qual pode ser compreendido que
essa afirmação dos brâmanes é apenas um dito no mundo.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. “O que
você pensa, grande rei? Suponha que um nobre arrombe casas, pilhe posses,
pratique o roubo, faça emboscadas nas estradas, ou seduza a mulher de outrem, e
se os seus homens o prenderem e o apresentarem dizendo: ‘Senhor, este é o
culpado; ordene o castigo que deseja para ele,’ como você o trataria?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Nós o
executaríamos, Mestre Kaccana, ou nós o multaríamos, ou nós o baniríamos, ou faríamos
com ele aquilo que ele merece. Por que isso? Porque ele perdeu o seu status
anterior de nobre e é identificado simplesmente como um ladrão.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O que você
pensa, grande rei? Suponha que um brâmane ... um comerciante ... um trabalhador
arrombe casas ...<span style="mso-spacerun: yes">  </span>ou seduza a mulher de
outrem, e se os seus homens o prenderem e o apresentarem dizendo: ‘Senhor, este
é o culpado; ordene o castigo que deseja para ele,’ como você o trataria?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Nós o
executaríamos, Mestre Kaccana, ou nós o multaríamos, ou nós o baniríamos, ou
faríamos com ele aquilo que ele merece. Por que isso? Porque ele perdeu o seu
status anterior de brâmane ... de comerciante ... de trabalhador e é<span
style="mso-spacerun: yes">  </span>identificado simplesmente como um ladrão.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O que você
pensa, grande rei? Em sendo assim, então essas quatro castas são todas a mesma
coisa, ou não são, ou qual o seu parecer nesse caso?” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Com
certeza, em sendo assim, Mestre Kaccana, então essas quatro castas são todas a
mesma coisa: não há nenhuma diferença entre elas que eu possa ver.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Essa também
é uma maneira, grande rei, através através da qual pode ser compreendido que
essa afirmação dos brâmanes é apenas um dito no mundo.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. “O que
você pensa, grande rei? Suponha que um nobre, que tenha raspado o cabelo e a
barba, vestisse o manto de cor ocre e seguisse a vida santa, abstendo-se de
matar seres vivos, de tomar aquilo que não é dado e da linguagem mentirosa.
Evitando comer à noite, comendo apenas numa parte do dia, sendo celibatário,
virtuoso e de bom caráter. Como você o trataria?” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Nós o
homenagearíamos, Mestre Kaccana, ou nos levantaríamos, ou o convidaríamos para
que ele se sentasse; ou o convidaríamos para que aceitasse mantos, alimentos,
moradia ou medicamentos; ou nós lhe proveríamos guarda, defesa e proteção sob a
lei. Por que isso? Porque ele perdeu o seu status anterior de nobre e é
identificado simplesmente como um contemplativo.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O que você
pensa, grande rei? Suponha que um brâmane ... um comerciante ... um
trabalhador, que tenha raspado o cabelo e a barba ... sendo celibatário,
virtuoso e de bom caráter. Como você o trataria?” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Nós o
homenagearíamos, Mestre Kaccana, ou nos levantaríamos, ou o convidaríamos para
que ele se sentasse; ou o convidaríamos para que aceitasse mantos, alimentos,
moradia ou medicamentos; ou nós lhe proveríamos guarda, defesa e proteção sob a
