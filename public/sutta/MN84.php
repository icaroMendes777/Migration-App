<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. Quando
isso foi dito o Rei Avantiputta de Madhura disse para o venerável Maha Kaccana:
</span>“Magnífico, Mestre Kaccana! Magnífico, Mestre Kaccana! Mestre Kaccana
esclareceu o Dhamma de várias formas, como se tivesse colocado em pé o que
estava de cabeça para baixo, revelasse o que estava escondido, mostrasse o
caminho para alguém que estivesse perdido ou segurasse uma lâmpada no escuro
para aqueles que possuem visão pudessem ver as formas. Nós buscamos refúgio no
Mestre Kaccana, no Dhamma e na Sangha dos bhikkhus. Que o Mestre Kaccana nos
aceite como discípulos leigos que nele buscaram refúgio para o resto da sua vida.”<span
lang=PT-BR style='mso-ansi-language:PT-BR'><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não busque
refúgio em mim, grande rei. Busque refúgio no mesmo Abençoado, um arahant,
perfeitamente iluminado, em quem eu busquei refúgio.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Onde ele
está vivendo agora, esse Abençoado, um arahant, perfeitamente iluminado, Mestre
Kaccana?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Esse
Abençoado, um arahant, perfeitamente iluminado, realizou o parinibbana, grande
rei.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. “Se
ouvíssemos que o Abençoado estava a dez léguas, nós percorreríamos as dez
léguas para ver esse Abençoado, um arahant, perfeitamente iluminado. Se ouvíssemos que
o Abençoado estava a vinte léguas ... trinta léguas ... quarenta léguas ...
cinqüenta léguas ... cem léguas, nós percorreríamos as cem léguas para ver esse
Abençoado, um arahant, perfeitamente iluminado. Mas visto que esse Abençoado
realizou o parinibbana, nós buscamos refúgio no Abençoado, no Dhamma e na
Sangha dos bhikkhus. Que a partir de hoje o Mestre Kaccana se recorde de mim
como um discípulo leigo que buscou refúgio pelo resto da sua vida.”<o:p></o:p></span></p>


