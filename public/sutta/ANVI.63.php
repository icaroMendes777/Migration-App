o que se deseja é sofrimento; em resumo, os cinco agregados influenciados pelo apego
são sofrimento. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;E qual é a razão
porque o sofrimento se manifesta? Desejo é a razão porque o sofrimento se manifesta. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;E qual é a
diversidade do sofrimento? Existe sofrimento forte e fraco, que desaparece lentamente e
que desaparece rapidamente. Essa é chamada a diversidade do sofrimento. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;E qual é o resultado
do sofrimento? Existem alguns casos em que uma pessoa tomada pela dor, sua mente
exausta, ela sofre, chora, lamenta, bate no seu peito e fica perturbada. Ou
outra tomada pela dor, sua mente exausta, começa uma busca no exterior, ‘Quem
conhece uma maneira para terminar com esta dor?' Eu lhes digo, bhikkhus, que o
sofrimento resulta ou em perturbação ou em busca. Esse é chamado o resultado do
sofrimento. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;E qual é a cessação
do sofrimento? A partir da cessação do desejo, cessa o sofrimento; e justamente este
nobre caminho óctuplo – entendimento correto, pensamento correto, linguagem
correta, ação correta, modo de vida correto, esforço correto, atenção plena
correta, concentração correta – é o caminho da prática que conduz à cessação do
sofrimento. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Agora quando um nobre
discípulo compreende desse modo o sofrimento, a causa porque o sofrimento se manifesta, a
diversidade do sofrimento, o resultado do sofrimento, a cessação do sofrimento, e o caminho
da prática que conduz à cessação do sofrimento, então ele compreende esta
penetrante vida santa como sendo a cessação do sofrimento.&quot;<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><span style="mso-spacerun:
yes"> </span>“E esta é uma explicação penetrante que é uma explicação do
Dhamma.” </span></p>


