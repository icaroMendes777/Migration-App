sensual e que provocam a cobiça. Se um bhikkhu sente prazer com elas, as acolhe
e permanece atado a elas, o deleite irá surgir nele. Com o surgimento do
deleite, ocorre o surgimento do sofrimento, eu digo. Sons conscientizados através do
ouvido … Aromas conscientizados através do nariz … Sabores conscientizados através da
língua … Tangíveis conscientizados através do corpo que são desejáveis, agradáveis e
fáceis de serem gostados, conectados com o desejo sensual e que provocam a
cobiça. Mas esses não são os prazeres dos sentidos; na disciplina dos nobres eles são
chamados de objetos dos prazeres dos sentidos. O prazer sensual de uma pessoa é a atenção apaixonada.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
