<p class=Normal> "Bhikkhus, há essas cinco ocasiões desfavoráveis para o esforço. Quais cinco? <p>

<p class=Normal> (1) "Aqui, um bhikkhu é velho, subjugado pela idade. Essa é a primeira ocasião desfavorável para o esforço. <p>

<p class=Normal> (2) "Novamente, um bhikkhu é enfermo, subjugado pela enfermidade. Essa é a segunda ocasião desfavorável para o esforço. <p>

<p class=Normal> (3) "Novamente, há fome, a colheita foi pobre, uma época em que é difícil conseguir comida esmolada e não é fácil subsistir. Essa é a terceira ocasião desfavorável para o esforço. <p>

<p class=Normal> (4) "Novamente, há perigo, turbulência na floresta, e as pessoas do campo, montadas sobre os seus veículos fogem para todos os lados. Essa é a quarta ocasião desfavorável para o esforço. <p>

<p class=Normal> (5) "Novamente, há um cisma na Sangha, e quando há um cisma na Sangha, há insultos mútuos, ofensas mútuas, desprezo mútuo, rejeição mútua. Então aqueles que não confiam não obtêm confiança, enquanto que alguns dos que confiam mudam de idéia. Essa é a quinta ocasião desfavorável para o esforço. <p>

<p class=Normal>"Esses, bhikkhus, são as cinco ocasiões desfavoráveis para o esforço." <p>

<p class=Normal>"Há, bhikkhus, essas cinco ocasiões favoráveis para o esforço. Quais cinco? <p>

<p class=Normal> (1) "Aqui, um bhikkhu é jovem, com o cabelo negro, dotado com as bênçãos da juventude, no primeiro estágio da vida. Essa é a primeira ocasião favorável para o esforço. <p>

<p class=Normal> (2) "Novamente, um bhikkhu raramente está enfermo ou aflito, possuindo uma boa digestão, que não é nem demasiado fria nem demasiado quente, mas média, e adequada para o esforço. Essa é a segunda ocasião favorável para o esforço. <p>

<p class=Normal> (3) "Novamente, há abundância de comida, a colheita foi boa, sendo fácil conseguir comida esmolada e é fácil subsistir. Essa é a terceira ocasião favorável para o esforço. <p>

<p class=Normal> (4) "Novamente, as pessoas vivem em concórdia, com apreço mútuo, sem disputas, mesclando como leite e água, vendo um ao outro com bondade. Essa é a quarta ocasião favorável para o esforço. <p>

<p class=Normal> (5) "Novamente, não há tensão na Sangha - há concórdia, harmonia; sem disputas, com uma única recitação. Quando há concórdia na Sangha, não há insultos mútuos, ofensas mútuas, desprezo mútuo, rejeição mútua. Então aqueles que não confiam obtêm confiança, enquanto que aqueles que confiam aumentam sua confiança. Essa é a quinta ocasião favorável para o esforço. <p>

<p class=Normal>"Esses, bhikkhus, são as cinco ocasiões favoráveis para o esforço."<p>

