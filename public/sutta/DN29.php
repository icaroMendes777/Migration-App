<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><a name=T1></a>1. Assim
ouvi. Em certa ocasião, o Abençoado estava<span style='mso-spacerun:yes'> 
</span>entre os Sakyas, no prédio da escola, no manguezal pertencente à família
Vedhanna. Agora, naquela ocasião o Nigantha Nataputta havia acabado de falecer
em Pava. Com a sua morte os Niganthas se dividiram, partiram ao meio;
envolvidos em rixas e brigas, mergulhados em discussões, apunhalando uns aos
outros usando as palavras como adagas: “Você não compreende este Dhamma e
Disciplina. Eu compreendo este Dhamma e Disciplina. Como poderia você
compreender este Dhamma e Disciplina? O seu jeito está errado. O meu jeito está
certo. Eu sou consistente. Você é inconsistente. O que deveria ter sido dito
primeiro, você disse por último. O que deveria ter sido dito por último, você
disse primeiro. Aquilo que você pensou com tanto cuidado foi virado de pernas
para o ar. A sua doutrina foi refutada, foi provado que você está errado. Vá e
aprenda melhor, ou desembarace-se se puder!” Parecia que não havia nada além de
um massacre entre os discípulos de Nigantha Nataputta. E os seus discípulos
leigos vestidos de branco estavam desgostosos, consternados e desapontados com
os discípulos de Nigantha Nataputta. Eles se sentiam do mesmo modo em relação
ao Dhamma e Disciplina dele, que havia sido mal proclamado e mal exposto, que
não conduzia à emancipação, não conduzia à paz, exposto por alguém que não era
perfeitamente iluminado, e agora eles estavam com o santuário quebrado, sem um
refúgio. [<a href="#N1">1</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2. Então, o noviço Cunda, que havia passado o retiro das chuvas em Pava,
foi até o venerável Ananda que estava em Samagama e depois de cumprimentá-lo
sentou a um lado e contou o que estava acontecendo. O venerável Ananda
então disse para o noviço Cunda: “Amigo Cunda, essas são notícias que
devem ser relatadas ao Abençoado. Venha, vamos até o Abençoado para contar-lhe
isso.” - “Sim, venerável senhor,” o noviço Cunda respondeu.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. Então, o
venerável Ananda e o noviço Cunda foram juntos até o Abençoado e<span
style='mso-spacerun:yes'>  </span>contaram o que estava acontecendo. Ele disse:
“Cunda, esse é o caso de uma doutrina e disciplina que são mal proclamadas,
expostas de modo não edificante e ineficazes na tranqüilização das paixões
porque o seu proclamador não era perfeitamente iluminado.<o:p></o:p></span></p>



<p class=Normal><a name=T4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>4. “Em sendo esse o caso, Cunda, um discípulo vivendo de acordo com essa
doutrina não será capaz de manter uma conduta apropriada, nem segui-la, mas
desviar-se dela. Alguém poderá lhe dizer: ‘Amigo, isso é o que você recebeu,
essa é a sua oportunidade.[<a href="#N2">2</a>] <a name=R2></a>O seu mestre não
é perfeitamente iluminado, a sua doutrina e disciplina são mal proclamadas,
expostas de modo não edificante e ineficazes na tranqüilização das paixões. Você não será capaz de viver de acordo com essa
doutrina mas desviar-se dela.’ Nesse caso, Cunda, o mestre deve ser
censurado, a doutrina deve ser censurada, mas o pupilo é elogiável. E se alguém
dissesse para aquele pupilo: ‘Venha agora, venerável senhor, pratique de acordo
com a doutrina e disciplina proclamadas e transmitidas pelo seu mestre’ – então, aquele que
deu o encorajamento, a coisa encorajada e aquele que seguiu essa prática, irão
todos obter muito demérito. Por que? Porque a doutrina e disciplina são mal proclamadas,
expostas de modo não edificante e ineficazes na tranqüilização das paixões.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. “Mas
aqui, Cunda, há um mestre que não é perfeitamente iluminado, a sua doutrina e disciplina são mal proclamadas,
expostas de modo não edificante e ineficazes na tranqüilização das paixões, e um discípulo vive de acordo com aquela
doutrina e disciplina, age de acordo com elas. Alguém poderá lhe dizer: ‘Amigo, isso que você
recebeu não é bom, a sua oportunidade é ruim, o seu mestre não é perfeitamente
iluminado, a sua doutrina e disciplina são mal proclamadas,
expostas de modo não edificante e ineficazes na tranqüilização das paixões, no entanto você continua
vivendo de acordo com ela.’ Nesse caso, o mestre, a doutrina e o discípulo são
todos censuráveis. E se alguém dissesse: ‘Muito bem, venerável senhor, seguindo
essa prática você será bem sucedido,’ aquele que dá o encorajamento, a coisa
encorajada e aquele que ao ouvir esse encorajamento, faz um esforço ainda
maior, irão todos obter muito demérito. Por que? Porque a doutrina e disciplina são mal proclamadas,
expostas de modo não edificante e ineficazes na tranqüilização das paixões.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. “Mas
aqui há um mestre que é perfeitamente iluminado: a sua doutrina e disciplina são bem
proclamadas, expostas de modo edificante e eficazes na tranquilização das paixões
porque o seu proclamador é perfeitamente iluminado, porém um discípulo não vive
de acordo com essa doutrina e disciplina mas desvia-se dela. Nesse caso, alguém poderá
lhe dizer: ‘Amigo, você fracassou, você perdeu a sua oportunidade. O seu mestre
é perfeitamente iluminado, sua doutrina e disciplina são bem
proclamadas, expostas de modo edificante e eficazes na tranquilização das paixões, mas você não a
seguiu, você se desviou dela.’ Nesse caso, o mestre e a doutrina são
elogiáveis, mas o pupilo é censurável. E se alguém dissesse: ‘Muito bem,
venerável senhor, você deve seguir o ensinamento proclamado pelo seu mestre,’
então, aquele que dá o encorajamento, a coisa encorajada e aquele que praticar
dessa forma, irão todos obter muito mérito. Por que? Porque a doutrina e disciplina são bem
proclamadas, expostas de modo edificante e eficazes na tranquilização das paixões.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. “Mas
aqui, Cunda, há um mestre que é perfeitamente iluminado: a sua doutrina e disciplina são bem
proclamadas, expostas de modo edificante e eficazes na tranquilização das paixões
porque o seu proclamador é perfeitamente iluminado, e um discípulo a adotou, segue-a, praticando-a do modo apropriado e se
dedicando a ela. Alguém poderá lhe dizer: ‘Amigo, isso que você recebeu é bom,
essa é a sua oportunidade, o seu mestre é perfeitamente iluminado: a sua doutrina e disciplina são bem
proclamadas, expostas de modo edificante e eficazes na tranquilização das paixões
porque o seu proclamador é perfeitamente iluminado, e você está mantendo a doutrina e disciplina do seu mestre.’
Nesse caso, o mestre e a doutrina são elogiáveis, e o pupilo também é
elogiável. E se alguém dissesse para aquele pupilo: ‘Muito bem, venerável
senhor, seguindo essa prática você será bem sucedido,’ aquele que dá o
encorajamento, a coisa encorajada e aquele que ao ouvir esse encorajamento, faz
um esforço ainda maior, irão todos obter muito mérito. Por que? Porque assim é
quando a doutrina e disciplina são bem proclamadas, expostas de modo edificante
e eficazes na tranquilização das paixões, porque o seu proclamador é um mestre
perfeitamente iluminado. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. “Mas
agora, Cunda, suponha que um Mestre tenha surgido no mundo, um arahant
perfeitamente iluminado, e que a sua doutrina e disciplina sejam bem
proclamadas, expostas de modo edificante e eficazes na tranquilização das paixões. Mas os seus discípulos
não tenham obtido a proficiência naquele verdadeiro Dhamma, para eles a completa
pureza da vida santa ainda não se tornou clara e evidente de acordo com o seu
desdobramento lógico e ainda não se firmou entre eles, estando ainda no processo
de serem bem proclamadas entre os humanos por ocasião do falecimento do Mestre.
Em vista disso, Cunda, a morte do Mestre seria algo muito triste para os seus
discípulos. Por que? Eles pensariam: ‘Nosso Mestre surgiu no mundo em nosso
benefício, mas nós não obtivemos proficiência no verdadeiro Dhamma,
que ainda estava no processo de ser bem proclamado entre os humanos, e agora o nosso
Mestre faleceu!’ Em vista disso, a morte do Mestre seria algo muito triste para
os seus discípulos. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. “Mas
suponha que um Mestre tenha surgido no mundo, um arahant
perfeitamente iluminado, e que a sua doutrina e disciplina sejam bem
proclamadas, expostas de modo edificante e eficazes na tranquilização das paixões, e os seus discípulos tenham
obtido proficiência no verdadeiro Dhamma, que para eles a completa pureza da
vida santa tenha se tornado clara e evidente de acordo com o seu desdobramento
lógico e tenha se firmado entre eles, estando ao mesmo tempo bem proclamado
entre os humanos por ocasião do falecimento do Mestre. Em vista disso, a morte
do Mestre não seria algo muito triste para os seus discípulos. Por que? Ele
pensariam: ‘Nosso Mestre surgiu no mundo em nosso benefício, e nós obtivemos
proficiência no verdadeiro Dhamma, estando ao mesmo tempo bem proclamado
entre os humanos, e agora o nosso Mestre faleceu!’ Em vista disso, a morte do
Mestre não seria algo muito triste para os seus discípulos. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. “Mas,
Cunda, se a vida santa é assim circunstanciada e não há um mestre que é sênior,
ancião, ordenado faz tempo, maduro e veterano, então nesse caso, a vida santa
será imperfeita. Mas se existe um mestre assim, então nesse caso a vida santa
poderá ser aperfeiçoada. <o:p></o:p></span></p>



<p class=Normal><a name=T11></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>11. “Se, por um lado, houver um mestre que é sênior, mas se não houver
entre os bhikkhus, discípulos seniores que sejam experientes, treinados,
hábeis, que realizaram a paz e se libertaram do cativeiro,[<a href="#N3">3</a>]
<a name=R3></a>que são capazes de proclamar o verdadeiro Dhamma, capazes de
refutar através do verdadeiro Dhamma quaisquer doutrinas opostas que possam
surgir, e ao fazer isso dar uma exposição do Dhamma bem fundamentada, então, a
vida santa não estará aperfeiçoada. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. “Se por
outro lado, houver um mestre que é sênior e discípulos seniores, mas não houver
bhikkhus intermediários com essas qualidades, ... ou [apesar da presença
destes] não houver bhikkhus júniores com essas qualidades, ... não houver
bhikkhunis seniores, ... não houver bhikkhunis intermediárias ou júniores, ...
não houver discípulos leigos vestidos de branco, ... celibatários ou não, ... não houver discípulas leigas vestidas de branco, ...
celibatárias ou não, ou se o ensinamento não prosperar e florescer, não se
espalhar, não for bem conhecido, não for proclamado por todas partes, ... ou
[mesmo se essas condições forem satisfeitas] não tiver obtido o primeiro lugar
no apoio popular, então a vida santa não estará aperfeiçoada.<o:p></o:p></span></p>



<p class=Normal><a name=T13></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>13. “Se, no entanto, todas essas condições forem satisfeitas, então a
vida santa estará aperfeiçoada.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14. “Mas,
Cunda, eu agora surgi no mundo,  um arahant perfeitamente iluminado, o
Dhamma é bem proclamado, ... os meus discípulos são proficientes no verdadeiro
Dhamma, ... a completa pureza da vida santa se tornou clara e evidente de acordo
com o seu desdobramento lógico ... Mas agora sou um mestre envelhecido, ancião,
que seguiu a vida santa há muito tempo, e a minha vida está chegando ao fim. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. “No
entanto, entre os bhikkhus há mestres seniores que são experientes, treinados,
hábeis, que realizaram a paz e se libertaram do cativeiro, que são capazes de
proclamar o verdadeiro Dhamma, capazes de refutar através do verdadeiro Dhamma
quaisquer doutrinas opostas que possam surgir, e ao fazer isso dar uma
exposição do Dhamma bem fundamentada. E há bhikkhus intermediários que são
disciplinados e experientes, há noviços que são disciplinados e experientes, há bhikkhunis
seniores, intermediárias e júniores que são disciplinadas e experientes, há discípulos leigos
vestidos de branco, celibatários ou não, há discípulas leigas
vestidas de branco, celibatários ou não, e a vida santa que eu
proclamo prospera e floresce, está espalhada, é bem conhecida, proclamada por
todas partes, bem proclamada entre os humanos. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16. “Entre
todos os mestres que agora existem no mundo, Cunda, eu não vejo nenhum que
tenha alcançado uma posição de fama e a quantidade de discípulos <span
style='mso-tab-count:1'>        </span>que eu alcancei. De todas as ordens e
grupos no mundo, eu não vejo nenhuma tão famosa e com tão boa companhia como a
minha Sangha de bhikkhus. Se alguém fosse se referir a alguma vida santa como
sendo perfeitamente bem sucedida e perfeita, onde não falta nada e não há nada
supérfluo, bem proclamada na perfeição da sua pureza, é esta vida santa que
eles estariam descrevendo. Era Uddaka Ramaputta [<a href="#N4">4</a>] <a
name=R4></a>que costumava dizer: ‘Ele vê, mas não enxerga.’ O que seria isso,
que vendo, alguém não enxerga? É possível ver a lâmina de uma navalha afiada,
mas não enxergar o seu fio. Isso é o que ele queria dizer com: ‘Ele vê, mas não
enxerga.’ Ele se referia a uma coisa vulgar, mundana, ignóbil, sem nenhum
significado para a vida santa, uma mera navalha. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas se
alguém fosse usar essa expressão de modo apropriado:<span
style='mso-spacerun:yes'>  </span>‘Ele vê, mas não enxerga,’ seria da seguinte
forma. Aquilo que ele vê é um modo de vida santo que é completamente bem
sucedido e perfeito, onde não falta nada e não há nada supérfluo, bem
proclamado na perfeição da sua pureza. Se ele deduzisse alguma coisa disso,
pensando: ‘Desta forma ela será mais pura,’ ele não enxergaria isso. E se ele
fosse adicionar alguma coisa, pensando: ‘Desta forma ela será mais completa,’
ele não enxergaria isso. Esse é o significado do ditado: ‘Ele vê, mas não
enxerga.’ Portanto, Cunda, se alguém for se referir a algum modo de vida santo
como sendo completamente bem sucedido e perfeito, ... é esta vida santa que
eles estariam descrevendo. <o:p></o:p></span></p>



<p class=Normal><a name=T17></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>17. “Portanto, Cunda, todos vocês para quem eu ensinei essas verdades,
que realizei por mim mesmo através do conhecimento direto, devem se reunir e
recitá-las, colocando o significado lado a lado e o fraseado lado a lado, sem
dissensões, de modo que esta vida santa possa continuar e se manter durante
muito tempo pelo bem-estar e felicidade de muitos, com compaixão pelo mundo,
pelo bem, pelo bem-estar e felicidade de devas e humanos.[<a href="#N5">5</a>] <a
name=R5></a>E quais são as coisas que vocês devem recitar juntos? Elas são: os
quatro fundamentos da atenção plena; os quatro esforços corretos; as quatro
bases para o poder; as cinco faculdades dominantes; os cinco poderes; os sete
fatores da Iluminação; o Nobre Caminho Óctuplo. Essas são as coisas que vocês
devem recitar juntos.<o:p></o:p></span></p>



<p class=Normal><a name=T18></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>18. “E assim vocês devem praticar,<span style='mso-spacerun:yes'> 
</span>reunidos em harmonia e sem dissensões. Se um companheiro cita o Dhamma
na assembléia e se você pensar que ele entendeu mal o significado ou o fraseado
é equivocado, você não deve nem aplaudir e nem rejeitar, mas dizer para ele:
‘Amigo, se você quer dizer assim ou assado, você deveria colocá-lo desta ou
daquela forma: qual é o mais apropriado?’ ou: ‘Se você diz assim ou assado,
você quer dizer isto ou aquilo: qual é o mais apropriado?’ Se ele responder:
‘Este significado é expressado melhor assim do que assado’, ou : ‘O significado
deste fraseado é isto ao invés daquilo,’ então, as palavras dele não devem ser
nem rejeitadas e nem depreciadas, mas você deve explicar a ele com cuidado o
significado correto e o fraseado correto. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>19.
“Novamente, Cunda, se um companheiro na vida santa cita o Dhamma na assembléia
e se você pensar que ele entendeu mal o significado embora o fraseado seja
correto, você não deve nem aplaudir e nem rejeitar, mas dizer para ele: ‘Amigo,
essas palavras podem significar isto ou aquilo: qual significado é o mais
apropriado?’ E se ele responder: ‘Elas significam isto,’ então as palavras dele
não devem ser nem rejeitadas e nem depreciadas, mas você deve explicar a ele
com cuidado o significado correto.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>20. “E de
modo semelhante, se você pensar que o significado é correto mas o fraseado é
equivocado, ... você deve explicar a ele com cuidado o significado correto e o
fraseado correto.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>21. “Mas, Cunda,
se você pensar que o significado está correto e o fraseado está correto, ...
você deve dizer: “Muito bem![<a href="#N6">6</a>] <a name=R6></a>E você deve
aplaudi-lo e cumprimentá-lo dizendo: ‘Nós somos afortunados, nós somos
abençoados por tê-lo encontrado, amigo, um companheiro na vida santa que é tão
bem versado em ambos, no significado e no fraseado!’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>22. “Cunda,
eu não ensino o Dhamma para coibir as contaminações que surgem apenas nesta
vida. Eu não ensino o Dhamma somente para a destruição delas nas vidas futuras,
mas para coibi-las nesta vida, bem como para a sua destruição nas vidas
futuras. Por conseguinte, Cunda, que o manto que eu o autorizei a usar seja
usado apenas para protegê-lo do frio, protegê-lo do calor, protegê-lo do
contato com moscas, mosquitos, vento, sol e criaturas rastejantes, e para
ocultar as partes íntimas. Que a comida esmolada que eu o autorizei a pedir
seja suficiente apenas para manter a resistência e continuidade do corpo, como
forma de dar um fim ao desconforto e para auxiliar a vida santa, considerando:
‘Dessa forma darei um fim às antigas sensações (de fome) sem despertar novas
sensações (de comida em excesso) e serei saudável e sem culpa e viverei em
comodidade.’ Que a moradia que eu o autorizei a ter seja somente para proteção
do frio, para proteção do calor, para proteção do contato com moscas,
mosquitos, vento, sol e criaturas rastejantes e somente com o propósito de
evitar os perigos do clima e para desfrutar do isolamento. Que os medicamentos
que eu o autorizei a tomar sejam somente para proteção contra sensações
aflitivas que já surgiram e para se beneficiar da boa saúde. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>23. “Pode
ser, Cunda, que os errantes de outras seitas digam: ‘Os contemplativos que
seguem o Sakya têm o vício de uma vida dedicada ao prazer.’ Em sendo assim,
eles devem ser perguntados: ‘Que tipo de vida dedicada ao prazer, amigo? Pois
esse tipo de vida pode assumir muitas formas.’ Há, Cunda, quatro tipos de vida
dedicadas ao prazer que são baixas, vulgares, grosseiras, ignóbeis e que não
trazem benefício, não conduzem ao desencantamento, desapego, cessação, paz,
conhecimento direto, iluminação e Nibbana. Quais são elas? Primeiro, uma pessoa
tola sente prazer e gozo em matar seres vivos. Segundo, alguém sente prazer e
gozo em tomar aquilo que não é dado. Terceiro, alguém sente prazer e gozo em
dizer mentiras. Quarto, alguém se entrega à paixão e gozo dos prazeres dos
cinco sentidos. Esses são os quatro tipos de vida dedicadas ao prazer que são
baixas, vulgares, ... não conduzem ao desencantamento, ... iluminação e
Nibbana.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>24. “E pode
ser que os errantes de outras seitas digam: ‘Os discípulos do Sakya são dados a
esses quatro tipos de busca do prazer?’ A resposta deve ser: ‘Não!’ pois eles
não estariam falando a seu respeito da forma correta, eles o estariam
caluniando com afirmações falsas e mentirosas. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Cunda, há
esses quatro tipos de vida dedicadas ao prazer que por inteiro conduzem ao
desencantamento, desapego, cessação, paz, conhecimento direto, iluminação e
Nibbana. Quais são elas? Primeiro, um bhikkhu afastado dos prazeres sensuais, afastado das qualidades não hábeis, entra e permanece no primeiro jhana, que é caracterizado pelo pensamento aplicado e sustentado, com o êxtase e felicidade nascidos do afastamento. Abandonando o pensamento aplicado e sustentado, um bhikkhu entra e permanece no segundo jhana, que é caracterizado pela segurança interna e perfeita unicidade da mente, sem o pensamento aplicado e sustentado, com o êxtase e felicidade nascidos da concentração. Abandonando o êxtase, um bhikkhu entra e permanece no terceiro jhana que é caracterizado pela felicidade sem o êxtase, acompanhada pela atenção plena, plena consciência e equanimidade, acerca do qual os nobres declaram: ‘Ele permanece numa estada feliz, equânime e plenamente atento.’ Com o completo desaparecimento da felicidade, um bhikkhu entra e permanece no quarto jhana, que possui nem felicidade nem sofrimento, com a atenção plena e a equanimidade purificadas.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Esses são
os quatro tipos de vida dedicadas ao prazer que por inteiro conduzem ao
desencantamento, desapego, cessação, paz, conhecimento direto, iluminação e
Nibbana. Portanto se os errantes de outras seitas dissessem que os discípulos
do Sakya são dados a esses quatro tipos de busca do prazer, a resposta deve
ser: ‘Sim,’ pois eles estariam falando a seu respeito da forma correta, eles
não o estariam caluniando com afirmações falsas e mentirosas.<o:p></o:p></span></p>



<p class=Normal><a name=T25></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>25. “Então esses errantes poderão perguntar: ‘Muito bem , então, aqueles
dados a esses quatro tipos de busca do prazer – quantos frutos, quantos
benefícios eles podem esperar obter?’ E você deveria responder: ‘Eles podem
esperar obter quatro frutos, quatro benefícios. Quais são eles? O primeiro é
quando um bhikkhu com a completa destruição de três grilhões entra na
correnteza, não mais destinado aos mundos inferiores, com o destino fixo, ele
tem a iluminação como destino; o segundo é quando um bhikkhu com a completa
destruição de três grilhões e com a atenuação da cobiça, raiva e delusão, se
torna um daqueles que retorna uma vez, e tendo retornado uma vez mais a este
mundo, dará um fim ao sofrimento; o terceiro é quando um bhikkhu com a completa
destruição dos cinco primeiros grilhões, renasce espontaneamente nas Moradas
Puras e lá irá realizar o parinibbana sem nunca mais retornar daquele mundo.
O quarto é quando um bhikkhu com a completa destruição das impurezas, permanece
num estado livre de impurezas com a libertação da mente e a libertação através da
sabedoria, tendo conhecido e manifestado isso para si mesmo no aqui e agora.
Esses são os quatro frutos e quatro benefícios obtidos por aquele que é dado a
esses quatro tipos de busca do prazer.’<o:p></o:p></span></p>



<p class=Normal><a name=T26></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>26. “Então, esses errantes poderão dizer: ‘As doutrinas dos discípulos
do Sakya não estão bem fundamentadas.’ A resposta deveria ser: ‘Amigos, o
Abençoado que sabe e vê proclamou e ensinou para os seus discípulos princípios
que não devem ser transgredidos enquanto durar a vida. Tal qual um pilar com um
ferrolho ou um pilar de ferro com uma base profunda, bem plantado e inabalável,
inabaláveis são essas doutrinas que ele ensinou. E qualquer bhikkhu que é um
Arahant, cujas impurezas foram destruídas, que viveu a vida santa, fez o que
devia ser feito, depôs o fardo, alcançou o verdadeiro objetivo, destruiu os
grilhões da existência e está completamente libertado através do conhecimento
supremo, é incapaz de fazer nove coisas:<span style='mso-spacerun:yes'> 
</span>(1) Ele é incapaz de tirar a vida de um ser vivo de modo deliberado; (2)
ele é incapaz de tomar aquilo que não for dado para constituir um roubo (3) ele
é incapaz de manter relações sexuais; (4) ele é incapaz de contar uma mentira
deliberada; (5) ele é incapaz de armazenar coisas para o gozo dos sentidos como
fazia na vida em família; (6) ele é incapaz de agir de modo incorreto devido ao
desejo; (7) ele é incapaz de agir de modo incorreto devido à raiva; (8) ele é
incapaz de agir de modo incorreto devido à delusão; (9) ele é incapaz de agir
de modo incorreto devido ao medo. Essas são as nove coisas que um Arahant,
cujas impurezas foram destruídas ...”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>27. “Ou
esses errantes poderão dizer: ‘Com relação ao passado, o contemplativo Gotama
demonstra conhecimento e insight ilimitados, mas não com relação ao futuro, com
relação ao que será e como será.’ Seria supor que o conhecimento e o insight
referente a uma coisa são produzidos pelo conhecimento e insight em relação a
algo diferente, como imaginam os tolos. Com relação ao passado, o Tathagata tem
conhecimento das vidas passadas. Ele é capaz de se recordar o quanto queira do
passado. Quanto ao futuro, este conhecimento, que tem origem na iluminação
surge nele: ‘Este é o último nascimento, não haverá mais ser/existir.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>28. “Se
‘passado’ se refere ao que não é fato, a fábulas, ao que não traz benefício, o
Tathagata não responde. Se ‘passado’  se refere ao que é fato, não é fábula, mas que
não traz benefício, o Tathagata não responde. Mas se ‘passado’ se refere ao que
é fato, não é fábula, e que traz benefício, então, o Tathagata sabe o momento
mais apropriado para responder. O mesmo se aplica ao futuro e ao presente.
Portanto, Cunda, o Tathagata é chamado aquele que declara o tempo, o fato, o
benefício, o Dhamma e a Disciplina. É por isso que ele é chamado Tathagata.<o:p></o:p></span></p>



<p class=Normal><a name=T29></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>29. “Cunda, tudo aquilo no mundo, com os seus devas, maras e brahmas,
esta população com seus contemplativos e brâmanes, seus príncipes e o povo, que
é<span style='mso-spacerun:yes'>  </span>visto, ouvido, sentido,
conscientizado, buscado, procurado, ponderado pela mente: o Tathagata despertou
completamente para tudo isso. Por isso ele é chamado o Tathagata. Desde a noite
em que o Tathagata despertou completamente para a insuperável perfeita
iluminação até a noite na qual ele realize o parinibbana, tudo aquilo que o
Tathagata disse, falou, explicou é assim (<i style='mso-bidi-font-style:normal'>tatha</i>)
e não de outra forma. Por isso ele é chamado o Tathagata. Neste mundo, com os
seus deuses, maras e brahmas, esta população com seus contemplativos e brâmanes,
seus príncipes e o povo, o Tathagata é o Conquistador, o Não-conquistado,
Omnisciente, Todo Poderoso. Por isso ele é chamado o Tathagata.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>30. “Ou
esses errantes poderão dizer: ‘O Tathagata existe após a morte: somente isso é
verdadeiro, todo o restante é falso.’ A resposta deveria ser: ‘Amigo, isso não
foi declarado pelo Abençoado.’ ... ‘O Tathagata não existe após a morte?’ .. ‘O
Tathagata tanto existe como não existe após a morte?’ ... ‘O Tathagata nem existe,
nem não existe após a morte?’ A resposta deveria ser: ‘Amigo, isso não foi
declarado pelo Abençoado.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>31. “Então,
eles poderão dizer: ‘Porque o contemplativo Gotama não declarou isso?’ A
resposta deveria ser: ‘Amigos, Porque não traz beneficio, não pertence aos
fundamentos da vida santa, não conduz ao desencantamento, ao desapego, à
cessação, à paz, ao conhecimento direto, à iluminação, a Nibbana. É por isso
que o Abençoado não declarou isso.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>32. “Ou
eles poderão dizer: ‘Muito bem, amigo, então o que<span
style='mso-spacerun:yes'>  </span>declarou o contemplativo Gotama?’ A resposta
deveria ser: ‘Isto é sofrimento’ – ele declarou. ‘Esta é a origem do
sofrimento’ – ele declarou. ‘Esta é a cessação do sofrimento’ – ele declarou.
‘Este é o caminho que conduz à cessação do sofrimento’ – ele declarou.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>33. “Então,
eles poderão dizer: ‘Por que o contemplativo Gotama declarou isso?’ A resposta
deveria ser: ‘Amigos, Porque isso traz beneficio, pertence aos fundamentos da vida
santa, conduz ao desencantamento, ao desapego, à cessação, à paz, ao
conhecimento direto, à iluminação, a Nibbana. É por isso que o Abençoado
declarou isso.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>34. “Cunda,
aquelas especulações sobre o começo das coisas eu lhe expliquei como devem ser
expostas, devo agora explicar-lhe como elas não devem ser expostas? [<a
href="#N7">7</a>] <a name=R7></a>E da mesma forma com relação ao futuro? Quais
são as especulações com relação ao passado ...? Há contemplativos e Brâmanes
que dizem e acreditam que: ‘O eu e o mundo são eternos: somente isso é
verdadeiro, todo o restante é falso’<span style='mso-spacerun:yes'>  </span>‘O
eu e o mundo não são eternos.’ ... ‘O eu e o mundo são tanto, eternos como não
eternos.’ ... ‘O eu e o mundo não são nem eternos e nem não eternos.’ ‘O eu e o
mundo são criados por si mesmos.’ ... ‘Eles são criados por outrem.’ ... ‘Eles
são tanto, criados por si mesmos como por outrem.’ ... ‘Eles não são nem criados
por si mesmos e nem por outrem, mas têm origem fortuita.’ E da mesma forma com
relação ao prazer e a dor. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>35.-36.
“Agora, Cunda, eu vou até esses contemplativos e Brâmanes que têm essas idéias
e ao serem perguntados eles confirmam que essas são as suas idéias, eu não
aceito as suas afirmações. Por que não? Porque, Cunda, diferentes seres têm
opiniões diferentes com relação a esses assuntos. Nem considero essas teorias
em pé de igualdade com a minha, quanto menos superior. Eu sou superior em
relação a eles no que diz respeito à exposição suprema. Quanto às especulações
sobre o começo das coisas que eu lhe expliquei como devem ser expostas, por que
deveria eu agora explicar-lhe como elas não devem ser expostas?<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>37. “E em
relação aos especuladores sobre o futuro? Há alguns contemplativos e Brâmanes
que dizem: ‘Após a morte o eu é material e intacto’; ‘... imaterial’; ‘...
‘tanto material como imaterial’; ‘ ... nem material e nem imaterial’; ‘O eu é
perceptivo após a morte’; ‘ ... não perceptivo’; ‘ ... tanto’; ‘ ... nenhum dos
dois’; ‘O eu é aniquilado, destruído, não existe depois da morte: somente isso
é verdadeiro, todo o restante é falso.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>38,-39.
“Agora, Cunda, eu vou até esses contemplativos e Brâmanes que têm essas idéias
e ao serem perguntados eles confirmam que essas são as suas idéias, eu não
aceito as suas afirmações. Por que não? Porque, Cunda, diferentes seres têm
opiniões diferentes em relação a esses assuntos. Nem considero essas teorias em
pé de igualdade com a minha, quanto menos superior. Eu sou superior com relação
a eles no que diz respeito à exposição suprema. Quanto às especulações sobre o
futuro que eu lhe expliquei como devem ser expostas, por que deveria eu agora
explicar-lhe como elas não devem ser expostas?<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>40. “E,
Cunda, para a destruição de todas essas idéias em relação ao passado e o
futuro, para transcendê-las, eu ensinei e estabeleci os quatro fundamentos da
atenção plena. Quais quatro? Aqui, Cunda, um bhikkhu permanece contemplando o
corpo como um corpo, ardente, plenamente consciente e com atenção plena, tendo
colocado de lado a cobiça e o desprazer pelo mundo. Ele permanece contemplando
as sensações como sensações, ... mente como mente, ... ele permanece
contemplando os objetos mentais como objetos mentais, ardente, plenamente
consciente e com atenção plena, tendo colocado de lado a cobiça e o desprazer
pelo mundo. É assim, Cunda, que eu ensinei e estabeleci os quatro fundamentos
da atenção plena para a destruição de todas essas idéias em relação ao passado
e o futuro, para transcendê-las.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>41. Durante
todo esse tempo o Venerável Upavana [<a href="#N8">8</a>] <a name=R8></a>estava
em pé atrás do Abençoado, ventilando-o. Então, ele disse ao Abençoado: &quot;É
admirável, venerável senhor, é maravilhoso! Venerável senhor, esta exposição do
Dhamma é encantadora – muito encantadora! Venerável senhor, qual é o nome deste
discurso do Dhamma?” “Quanto a isso, Upavana, você poderá se lembrar deste
discurso do Dhamma como ‘O Discurso Encantador.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o
que disse o Abençoado. O Venerável Upavana<span style='mso-spacerun:yes'> 
</span>ficou satisfeito e contente com as palavras do Abençoado.<o:p></o:p></span></p>

