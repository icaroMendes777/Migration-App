<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
suponham que um cozinheiro tolo, incompetente, inábil, ofereça a um rei ou ao ministro
de um rei vários tipos de caril (curry): azedos, amargos, apimentados, doces,
alcalinos, não alcalinos, salgados, não salgados. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aquele
cozinheiro tolo, incompetente, inábil, não apanha os sinais das preferências do
seu patrão: ‘Hoje meu patrão prefere este caril ou ele estende a mão para
aquele caril, ou ele pega uma grande porção deste caril, ou ele elogia aquele
caril. Hoje o meu patrão prefere o caril azedo ... Hoje meu patrão prefere o
caril amargo ... caril apimentado ... caril doce ... caril alcalino ... caril
não alcalino ... caril salgado ... Hoje o meu patrão prefere o caril não
salgado ou ele estende a mão para o caril não salgado, ou ele pega uma grande
porção do caril não salgado, ou ele elogia o caril não salgado.’ <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aquele
cozinheiro tolo, incompetente, inábil, não é recompensado com roupas ou
dinheiro ou prêmios. Por que isso? Porque, bhikkhus, aquele cozinheiro tolo,
incompetente, inábil não apanha os sinais das preferências do seu patrão.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Da mesma forma, bhikkhus,
aqui algum bhikkhu tolo, incompetente, inábil, permanece contemplando o corpo
como um corpo, ardente, plenamente consciente e com atenção plena, tendo
colocado de lado a cobiça e o desprazer pelo mundo. Enquanto ele permanece
contemplando o corpo como um corpo, a sua mente não fica concentrada, os
obstáculos não são abandonados, ele não apanha aquele sinal. Ele permanece
contemplando as sensações como sensações ... a mente como mente ... os objetos
mentais como objetos mentais,<span style='mso-spacerun:yes'>  </span>ardente,
plenamente consciente e com atenção plena, tendo colocado de lado a cobiça e o
desprazer pelo mundo. Enquanto ele permanece contemplando os objetos mentais
como objetos mentais, a sua mente não fica concentrada, os obstáculos não são
abandonados, ele não apanha aquele sinal. [<a href="#N1">1</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aquele bhikkhu
tolo, incompetente, inábil, não é recompensado com um estado prazeroso no aqui
e agora, e ele não obtém a atenção plena e a plena consciência. Por que isso?
Porque, bhikkhus, aquele bhikkhu tolo, incompetente, inábil não apanha o sinal
da sua própria mente.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Suponham, bhikkhus, que
exista um cozinheiro sábio, competente, hábil, que oferece a um rei ou ao
ministro de um rei vários tipos de caril: azedos, amargos, apimentados, doces,
alcalinos, não alcalinos, salgados, não salgados. Ele apanha os sinais das
preferências do seu patrão: ‘Hoje meu patrão prefere este caril ou ele estende
a mão para aquele caril, ou ele pega uma grande porção deste caril, ou ele
elogia aquele caril. Hoje o meu patrão prefere o caril azedo...Hoje meu patrão
prefere o caril amargo ... caril apimentado ... caril doce ... caril alcalino
... caril não alcalino ... caril salgado ... Hoje o meu patrão prefere o caril
não salgado ou ele estende a mão para o caril não salgado, ou ele pega uma
grande porção do caril não salgado, ou ele elogia o caril não salgado.’ <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aquele cozinheiro sábio,
competente, hábil é recompensado com roupas ou dinheiro ou prêmios. Por que
isso? Porque, bhikkhus, aquele cozinheiro sábio, competente, hábil, apanha os
sinais das preferências do seu patrão<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Da mesma forma, bhikkhus,
aqui algum bhikkhu sábio, competente, hábil, permanece contemplando o corpo
como um corpo, ardente, plenamente consciente e com atenção plena, tendo
colocado de lado a cobiça e o desprazer pelo mundo. Enquanto ele permanece
contemplando o corpo como um corpo, a sua mente fica concentrada, os obstáculos
são abandonados, ele apanha aquele sinal. Ele permanece contemplando as
sensações como sensações ... a mente como mente ... os objetos mentais como
objetos mentais,<span style='mso-spacerun:yes'>  </span>ardente, plenamente
consciente e com atenção plena, tendo colocado de lado a cobiça e o desprazer
pelo mundo. Enquanto ele permanece contemplando os objetos mentais como objetos
mentais, a sua mente fica concentrada, os obstáculos são abandonados, ele
apanha aquele sinal. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aquele bhikkhu sábio,
competente, hábil, é recompensado com um estado prazeroso no aqui e agora e ele
obtém a atenção plena e a plena consciência. Por que isso? Porque, bhikkhus,
aquele bhikkhu sábio, competente, hábil, apanha o sinal da sua própria mente.<a
