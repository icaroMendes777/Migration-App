<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião, o Abençoado estava perambulando pela região de Anga com
uma grande Sangha de bhikkhus, com quinhentos bhikkhus, até que por fim acabou
chegando num vilarejo brâmane denominado Campa. Em Campa, ele ficou no lago
Gaggara. Agora, naquela ocasião, o Brâmane Sonadanda era o regente de Campa,
uma propriedade real com muitos habitantes, rica em pastagens, árvores, rios e
grãos, uma concessão real, uma doação sagrada que lhe foi dada pelo rei Seniya
Bimbisara de Magadha.<o:p></o:p></span></p>



<p class=Normal><a name=T2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2. Os brâmanes chefes de família de Campa ouviram: “Gotama o
contemplativo, o filho dos Sakyas, que adotou a vida santa deixando o clã dos
Sakyas, andava perambulando em Anga com um grande número de bhikkhus, com
quinhentos bhikkhus, chegou em Campa e está no lago Gaggara. E acerca desse
mestre Gotama existe essa boa reputação: ‘Esse Abençoado é um arahant,
perfeitamente iluminado, consumado no verdadeiro conhecimento e conduta,
bem-aventurado, conhecedor dos mundos, um líder insuperável de pessoas
preparadas para serem treinadas, mestre de devas e humanos, desperto, sublime.
Ele declara - tendo realizado por si próprio com o conhecimento direto - este
mundo com os seus devas, maras e brahmas, esta população com seus contemplativos
e brâmanes, seus príncipes e povo. Ele ensina o Dhamma, com o significado e
fraseado corretos, que é admirável no início, admirável no meio, admirável no
final; e ele revela uma vida santa que é completamente perfeita e imaculada. É
bom poder encontrar alguém tão nobre.’ Assim, os brâmanes chefes de família de
Campa saíram de Campa em grupos e bandos e se dirigiram para o lago Gaggara.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. Agora,
naquela ocasião, o brâmane Sonadanda havia se retirado para o andar superior do
seu palácio para a sesta. Então, ele viu os brâmanes chefes de família de Campa
saindo de Campa em grupos e bandos e se dirigindo para o lago Gaggara. Ao
vê-los ele perguntou ao seu ministro: “Estimado ministro, porque os brâmanes
chefes de família de Campa estão saindo de Campa em grupos e bandos e se
dirigindo para o lago Gaggara?” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Senhor,
ali se encontra Gotama o contemplativo, o filho dos Sakyas, que adotou a vida
santa deixando o clã dos Sakyas, que andava perambulando em Anga ... (igual ao
verso 2) ... Eles estão indo ver esse Mestre Gotama.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
estimado ministro, vá até os brâmanes chefes de família de Campa e diga:
‘Senhores, o brâmane Sonadanda diz o seguinte: “Por favor esperem, senhores. O
Brâmane Sonadanda também irá ver o contemplativo Gotama.”’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
senhor,” o ministro respondeu e foi até os brâmanes chefes de família de Campa
e lhes disse a mensagem.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. Agora,
naquela ocasião, quinhentos brâmanes de várias regiões estavam em Campa por
questões de negócios. Eles ouviram: “Estão dizendo, que o brâmane Sonadanda
está indo ver o contemplativo Gotama.” Eles foram até o brâmane Sonadanda e
perguntaram: “Senhor, é verdade que você irá ver o contemplativo Gotama?” - 
“Assim é, senhores. Eu irei ver o contemplativo Gotama.” <o:p></o:p></span></p>



<p class=Normal><a name=T5></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>5. “Senhor, não vá ver o contemplativo Gotama. Não é apropriado, Mestre
Sonadanda, que você vá ver o contemplativo Gotama; ao invés disso, é apropriado
que o contemplativo Gotama venha vê-lo. Pois você, senhor, é bem nascido pelos
dois lados, com a descendência pura por parte da mãe e do pai por sete
gerações, inatacável e impecável com respeito ao nascimento. Sendo assim,
Mestre Sonadanda, não é apropriado que você vá ver o contemplativo Gotama; ao
invés disso, é apropriado que o contemplativo Gotama venha vê-lo. Você, senhor,
é rico, com grande riqueza e grandes posses. Você, senhor, é um mestre dos três
Vedas com os seus mantras, liturgia, fonologia e etimologia e as histórias como
quinto elemento; hábil em filologia e gramática, você é um perito em filosofia
natural e nas marcas de um grande homem. Você, senhor, é belo, atraente e
elegante, possuindo uma complexão de beleza suprema, com uma beleza e uma
presença sublime, digno de ser contemplado. Você, senhor, é virtuoso, com a
virtude madura, possuindo virtude amadurecida. Você, senhor, é um bom orador
com boa comunicação; você diz palavras que são corteses, distintas, imaculadas
e que transmitem o significado. Você, senhor, ensina os mestres de muitos e
ensina a recitação dos mantras para trezentos estudantes brâmanes. Você,
senhor, é honrado, respeitado, reverenciado, venerado e estimado pelo brâmane
Pokkharasati. Você, senhor, governa Campa, uma propriedade real com muitos
habitantes ... uma doação sagrada que lhe foi dada pelo rei Seniya Bimbisara.
Sendo assim, Mestre Sonadanda, não é apropriado que você vá ver o contemplativo
Gotama; ao invés disso, é apropriado que o contemplativo Gotama venha vê-lo.”<o:p></o:p></span></p>



<p class=Normal><a name=T6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>6. Quando isso foi dito, o brâmane Sonadanda disse para aqueles
brâmanes: “Agora, senhores, ouçam de mim porque é apropriado que eu vá ver o
contemplativo Gotama e porque não é apropriado que o Mestre Gotama venha me
ver. Senhores, o contemplativo Gotama é bem nascido pelos dois lados, com a
descendência pura por parte da mãe e do pai por sete gerações, inatacável e
impecável com respeito ao nascimento. Sendo assim, é apropriado que eu vá ver o
contemplativo Gotama e não é apropriado que o Mestre Gotama venha me ver. Senhores,
o contemplativo Gotama adotou a vida santa abandonando muito ouro e riquezas
armazenados em cofres e depósitos. Senhores, o contemplativo Gotama deixou a
vida em família e seguiu a vida santa ainda jovem, um homem jovem com o cabelo
negro, dotado com as bênçãos da juventude, na flor da juventude. Senhores, o
contemplativo Gotama raspou o cabelo e barba, vestiu o manto de cor ocre e
deixou a vida em família e seguiu a vida santa, embora a sua mãe e o seu pai
desejassem outra coisa e chorassem com o rosto coberto de lágrimas. Senhores, o
contemplativo Gotama é belo, atraente e elegante, possuindo uma complexão de
beleza suprema, com uma beleza e uma presença sublime, digno de ser
contemplado. Senhores, o contemplativo Gotama é virtuoso, com a virtude madura,
possuindo virtude amadurecida. Senhores, o contemplativo Gotama é um bom orador
com boa comunicação; ele diz palavras que são corteses, distintas, imaculadas e
que transmitem o significado. Senhores, o contemplativo Gotama é o mestre dos
mestres de muitos. Senhores, o contemplativo Gotama está livre da cobiça
sensual e sem vaidade pessoal. Senhores, o contemplativo Gotama possui a
doutrina da eficácia moral da ação, a doutrina da eficácia moral dos atos; ele
não busca prejudicar os brâmanes. Senhores, o contemplativo Gotama deixou uma
família aristocrática, uma das famílias nobres originais. Senhores, o
contemplativo Gotama deixou uma família rica, uma família com grande riqueza e
grandes posses. Senhores, as pessoas vêm de reinos remotos, de distritos
remotos para inquirir o contemplativo Gotama. Senhores, muitos milhares de
devas buscaram refúgio pelo resto da vida no contemplativo Gotama. Senhores,
acerca desse contemplativo Gotama existe essa boa reputação: ‘Esse Abençoado é
um arahant, perfeitamente iluminado, consumado no verdadeiro conhecimento e
conduta, bem-aventurado, conhecedor dos mundos, um líder insuperável de pessoas
preparadas para serem treinadas, mestre de devas e humanos, desperto,
sublime.’ Senhores, o contemplativo Gotama possui as trinta e duas marcas de um
Grande Homem. Senhores, o rei Seniya Bimbisara de Magadha e a sua esposa e
filhos buscaram refúgio pelo resto da vida no contemplativo Gotama. Senhores, o
rei Pasenadi de Kosala e a sua esposa e filhos buscaram refúgio pelo resto da
vida no contemplativo Gotama. Senhores, o brâmane Pokkharasati e a sua esposa e
filhos buscaram refúgio pelo resto da vida no contemplativo Gotama. Senhores, o
contemplativo Gotama chegou em Campa e está no lago Gaggara. Agora, todos os
contemplativos e brâmanes que chegam na nossa cidade são nossos hóspedes e
hóspedes devem ser honrados, respeitados, reverenciados e venerados por nós.
Visto que o contemplativo Gotama chegou em Campa, ele é nosso hóspede e como
nosso hóspede ele deve ser honrado, respeitado, reverenciado e venerado por
nós. Sendo assim, senhores não é apropriado que o Mestre Gotama venha me ver;
ao invés disso, é apropriado que eu vá ver o Mestre Gotama. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Senhores,
esse é o tanto de méritos do Mestre Gotama que eu aprendi, mas o mérito do
Mestre Gotama não está limitado a isso, pois o mérito do Mestre Gotama é
imensurável. Visto que o Mestre Gotama possui cada um desses fatores, não é
apropriado que ele venha me ver; ao invés disso, é apropriado que eu vá ver o
Mestre Gotama. Portanto, senhores, vamos todos ver o contemplativo Gotama.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. Ao ouvir
isso, os Brâmanes disseram para Sonadanda: “Senhor, visto que você elogia tanto
o contemplativo Gotama, então mesmo estando a uma distância de cem yojanas
daqui, seria apropriado que aqueles que têm fé fossem visitá-lo. Portanto,
senhor, iremos visitar o contemplativo Gotama.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>E assim,
Sonadanda foi com uma grande comitiva de Brâmanes para o lago Gaggara.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. Mas
depois que Sonadanda havia atravessado a floresta ele pensou: “Se eu fizer uma
pergunta ao contemplativo Gotama, ele poderá me dizer: ‘Essa pergunta, brâmane,
não deve ser feita dessa forma, a pergunta deve ser formulada assim,’ e então
esta comitiva poderá me desdenhar, dizendo: ‘Sonadanda é um tolo e inexperto,
ele não tem bom senso, ele não é nem mesmo capaz de formular a pergunta da
forma correta.’ E qualquer um que seja desdenhado, a sua reputação irá sofrer e
então a sua renda irá sofrer, pois a renda depende da reputação. Ou se o
contemplativo Gotama me fizer uma pergunta, minha resposta poderá não
satisfazê-lo, e ele poderá dizer: ‘Essa pergunta não deve ser respondida dessa
forma; é assim como esse problema deve ser resolvido.’ E então esta comitiva
poderá me desdenhar ... Mas por outro lado, se tendo vindo até aqui eu desse
meia volta sem me apresentar ao contemplativo Gotama, esta comitiva poderá me
desdenhar ...”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. Então,
Sonadanda foi até o Abençoado e ambos se cumprimentaram. Quando a conversa
cortês e amigável havia terminado ele sentou a um lado. Alguns brâmanes e
chefes de família homenagearam o Abençoado e sentaram a um lado; alguns
trocaram saudações corteses com ele e após a troca de saudações sentaram a um
lado; alguns ajuntaram as mãos em respeitosa saudação e sentaram a um lado;
alguns permaneceram em silêncio e sentaram a um lado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. Estando
ali sentado, Sonadanda estava tomado pela hesitação e com a mente povoada com
muitos pensamentos: “Se eu fizer uma pergunta ao contemplativo Gotama, ele
poderá me dizer: ‘Essa pergunta, brâmane, não deve ser feita dessa forma ...’
Se pelo menos o contemplativo Gotama me perguntasse algo do meu próprio
conhecimento dos Três Vedas! Aí eu poderia dar uma resposta que o satisfaria!”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. E o
Abençoado, sabendo com a sua própria mente os pensamentos na mente dele, pensou:
“Esse Sonadanda está preocupado. E se eu lhe perguntasse algo do seu próprio
campo de conhecimento como mestre dos Três Vedas?” Assim ele disse para
Sonadanda: “Através de quantas qualidades os Brâmanes reconhecem um Brâmane?
Como alguém declararia de acordo com a verdade e sem falsidade: ‘Eu sou um
Brâmane’?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. Então
Sonadanda pensou: “Aquilo que eu queria, esperava, desejava e ansiava aconteceu
– se pelo menos o contemplativo Gotama me perguntasse algo do meu próprio
conhecimento ... Agora posso lhe dar uma resposta que irá satisfazê-lo.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13.
Endireitando-se e olhando em volta para a assembléia, ele disse: “Mestre
Gotama, os Brâmanes declaram que alguém é um Brâmane capaz de afirmar ‘Eu sou
um Brâmane’ de acordo com a verdade e sem incorrer em falsidade quando ele
possui cinco qualidades. E quais são as cinco? Um Brâmane é bem nascido por
parte da mãe e por parte do pai, com descendência pura até a sétima geração;
ele é um mestre dos três Vedas com os seus mantras, liturgia, fonologia e etimologia
e as histórias como quinto elemento, hábil em filologia e gramática, um perito
em filosofia natural e nas marcas de um grande homem; ele é belo, atraente e
elegante, possuindo uma complexão de beleza suprema, com uma beleza e uma
presença sublime, digno de ser contemplado; ele é virtuoso, com a virtude
madura, possuindo virtude amadurecida; ele é estudado e sábio; e ele é o
primeiro ou o segundo a segurar a concha de sacrifício.[<a href="#N1">1</a>] <a
name=R1></a>Essas são as cinco qualidades de um verdadeiro Brâmane.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14. “Mas se
uma dessas cinco qualidades for omitida, não poderia alguém ser reconhecido
como um verdadeiro Brâmane, possuindo quatro dessas qualidades?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“É
possível, Mestre Gotama. Poderíamos deixar de lado a aparência, pois que
importa isso? Se um Brâmane possui as outras quatro qualidades ele pode ser
reconhecido como um verdadeiro Brâmane.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. “Mas
não poderia uma dessas quatro qualidades ser omitida, restando três através das
quais alguém possa ser reconhecido como um verdadeiro Brâmane?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“É
possível, Mestre Gotama. Poderíamos deixar de lado os mantras, pois que importa
isso? Se ele possuir as outras três qualidades ele pode ser reconhecido como um
verdadeiro Brâmane.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16. “Mas
não poderia uma dessas três qualidades ser omitida ...?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“É
possível, Mestre Gotama. Poderíamos deixar de lado o nascimento, pois que
importa isso? Se um brâmane é virtuoso, com a virtude madura, possuindo virtude
amadurecida, e se ele for estudado e sábio, e for o primeiro ou o segundo a
segurar a concha de sacrifício, então ele é capaz de afirmar ‘Eu sou um
Brâmane’ de acordo com a verdade e sem incorrer em falsidade.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17. Em
vista disso, os Brâmanes disseram para Sonadanda: “Não diga isso, Sonadanda não
diga isso! O Mestre Sonadanda está desprezando a aparência, os mantras e o
nascimento, ele na verdade está adotando a doutrina do próprio contemplativo
Gotama!”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18. Então,
o Abençoado disse para os Brâmanes: “Se vocês pensam que o Brâmane Sonadanda não
está se concentrando na sua tarefa, está empregando palavras incorretas, lhe
falta sabedoria e não está preparado para conversar com o contemplativo Gotama,
então que ele pare e vocês falem comigo. Mas se vocês pensam que ele é
estudado, fala da forma apropriada, é sábio e está preparado para conversar com
o contemplativo Gotama, então parem e deixem que ele fale.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>19. Então,
Sonadanda disse para os Brâmanes: “Não digam isso senhores. Eu não desprezo a
nossa aparência, os nossos mantras, ou o nosso nascimento.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>20. Agora,
naquela ocasião, o sobrinho de Sonadanda, um adolescente chamado Angaka, estava
sentado na assembléia e Sonadanda disse: “Senhores, vocês vêm o meu sobrinho
Angaka?” - “Sim, senhor.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Angaka é
belo, atraente e elegante, possuindo uma complexão de beleza suprema, com uma
beleza e uma presença sublime, digno de ser contemplado e não há ninguém nesta
assembléia que se iguale a ele exceto o contemplativo Gotama. Ele é um erudito
... eu fui o seu mestre dos mantras. Ele é bem nascido pelos dois lados ... eu
conheço os seus pais. Mas se Angaka matasse um ser vivo, tomasse aquilo que não
seja dado, cometesse adultério, dissesse mentiras e tomasse embriagantes – como
a aparência, ou os mantras, ou o nascimento o beneficiariam? Mas é por ser virtuoso
e por ser sábio, por conta disso, que um Brâmane é capaz de afirmar ‘Eu sou um
Brâmane’ de acordo com a verdade e sem incorrer em falsidade.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>21. “Mas,
Brâmane, se alguém omitisse um desses dois pontos, seria ele capaz de afirmar
‘Eu sou um Brâmane’ de acordo com a verdade e sem incorrer em falsidade?”<br> “Não,
Mestre Gotama. Pois a sabedoria é purificada pela virtude e a virtude é
purificada pela sabedoria: onde há uma, há a outra, o homem virtuoso possui
sabedoria e o homem sábio possui virtude, e a combinação de virtude e sabedoria
é chamada a coisa mais sublime no mundo. Tal qual uma mão lava a outra, ou um
pé ao outro, assim também a sabedoria é purificada pela virtude e essa
combinação é chamada a coisa mais sublime no mundo.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>22. “Assim
é, Brâmane, a sabedoria é purificada pela virtude e a virtude é purificada pela
sabedoria: onde há uma, há a outra, o homem virtuoso possui sabedoria e o homem
sábio possui virtude, e a combinação de virtude e sabedoria é chamada a coisa
mais sublime no mundo. Mas, Brâmane, o que é essa virtude e o que é essa
sabedoria?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Nós apenas
sabemos esse tanto, Mestre Gotama. Seria bom se o venerável pudesse explicar o
significado disso.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>23. “Então,
Brâmane, ouça e preste muita atenção ao que eu vou dizer.” – “Sim, venerável
senhor,” Sonadanda respondeu. O Abençoado disse o seguinte: <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Brâmane, um
Tathagata surge no mundo, um arahant, perfeitamente iluminado, consumado no
verdadeiro conhecimento e conduta, bem-aventurado, conhecedor dos mundos, um
líder insuperável de pessoas preparadas para serem treinadas, mestre de devas e
humanos, desperto, sublime. Ele declara - tendo realizado por si próprio com o
conhecimento direto - este mundo com os seus devas, maras e brahmas, esta
população com seus contemplativos e brâmanes, seus príncipes e povo. Ele ensina o
Dhamma, com o significado e fraseado corretos, que é admirável no início,
admirável no meio, admirável no final; e ele revela uma vida santa que é
completamente perfeita e imaculada. Um discípulo segue a vida santa e pratica a
virtude, (<a href="DN2.php#T41">DN 2.41- 62</a>); ele guarda as portas dos
meios dos sentidos (<a href="DN2.php#T64">DN 2.64-74</a>). Isso Brâmane é a
virtude. [<a href="#N2">2</a>] <a name=R2></a>Ele alcança os quatro jhanas, (<a
href="DN2.php#T75">DN 2.75-82</a>); ele realiza vários insights, (<a
href="DN2.php#T83">DN 2.83-95</a>), e a destruição das impurezas, (<a
href="DN2.php#T97">DN 2.97</a>). Assim, ele desenvolve a sabedoria. Isso
Brâmanes é sabedoria.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>24. Com
base nisso Sonadanda disse: “Magnífico, Mestre Gotama! Magnífico, Mestre
Gotama! Mestre Gotama esclareceu o Dhamma de várias formas, como se tivesse
colocado em pé o que estava de cabeça para baixo, revelasse o que estava
escondido, mostrasse o caminho para alguém que estivesse perdido ou segurasse
uma lâmpada no escuro para aqueles que possuem visão pudessem ver as formas. Eu
busco refúgio no Mestre Gotama, no Dhamma e na Sangha dos bhikkhus. Que o
Mestre Gotama me aceite como discípulo leigo que buscou refúgio para o
resto da vida! E que o Mestre Gotama e a Sangha dos bhikkhus concorde em
aceitar a refeição de amanhã.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O Abençoado
concordou em silêncio. Então, sabendo que o Abençoado havia concordado,
Sonadanda levantou-se do seu assento, e depois de homenagear o Abençoado,
mantendo-o à sua direita, partiu. Então, quando a noite terminou ele fez com
que se preparassem vários tipos de boa comida na sua própria residência e
quando tudo estava pronto ele anunciou: “É hora, Mestre Gotama, a refeição está
pronta.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>25. O
Abençoado se vestiu e carregando a sua tigela e o manto externo,
foi com a Sangha dos bhikkhus até a casa de Sonadanda e sentou num assento que
havia sido preparado. Então, com as próprias mãos Sonadanda serviu e satisfez o
Abençoado com os vários tipos de boa comida, e os jovens brâmanes serviram os
bhikkhus. Em seguida, quando o Abençoado havia terminado de comer e retirado a
mão da sua tigela, Sonadanda sentou a um lado, num assento mais baixo e disse
para o Abençoado:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>26. “Mestre
Gotama, se depois de ter entrado na assembléia eu me levantar e saudar o
Abençoado, a minha comitiva irá me desdenhar.[<a href="#N3">3</a>] <a name=R3></a>E
qualquer um que seja desdenhado, a sua reputação irá sofrer e então a sua renda
irá sofrer, pois a renda depende da reputação. Portanto, se ao entrar na
assembléia, eu juntar as palmas das minhas mãos em saudação, que o Mestre
Gotama tome isso como se eu tivesse levantado do meu assento. E se ao entrar na
assembléia eu tirar o meu turbante, que o Mestre Gotama tome isso como se eu
tivesse me curvado aos seus pés. Ou se, quando estiver montado na minha
carruagem, eu desmontar e saudar o Abençoado, a minha comitiva irá me
desdenhar ... Portanto, se estando montado na minha carruagem eu levantar a
aguilhada, que o Mestre Gotama tome isso como se eu tivesse desmontado da minha
carruagem; e se eu abaixar a minha mão, que o Mestre Gotama tome isso como se
eu tivesse curvado a minha cabeça aos seus pés.” [<a href="#N4">4</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>27. Então, depois que o Abençoado instruiu, motivou, estimulou e
encorajou</span><span lang=PT-BR style='font-size:10.0pt;font-family:Arial;
mso-ansi-language:PT-BR'> </span><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Sonadanda com um discurso do Dhamma, ele se levantou do seu assento e
partiu. <o:p></o:p></span></p>

