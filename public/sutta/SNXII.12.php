<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, existem esses quatro tipos de alimentos para a manutenção
dos seres que já nasceram e para o sustento daqueles que estão em busca de um
nascimento. Quais quatro? O alimento comida, grosseira ou sutil, o contato como
o segundo, a volição mental como o terceiro e a consciência como o quarto.
Estes são os quatro tipos de alimento para a manutenção dos seres que já
nasceram e para o sustento daqueles que estão buscando o nascimento.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isso
foi dito, o venerável Moliyaphagguna disse para o Abençoado: “Venerável senhor,
quem consome o alimento consciência?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Essa não é
uma pergunta válida,” o Abençoado respondeu. “Eu não digo ‘alguém consome.’ Se
eu dissesse ‘alguém consome,’ então nesse caso esta seria uma pergunta válida:
‘Venerável senhor, quem consome?’ Mas eu não falo dessa forma. Visto que não
falo dessa forma, se alguém me perguntar, ‘Venerável senhor, para que o
alimento consciência [é uma condição]?’ essa seria uma pergunta válida. A
resposta válida a essa pergunta é: ‘O alimento consciência é uma condição para
a produção de uma renovada existência futura. Quando há aquilo que foi gerado,
as seis bases dos sentidos são geradas; com as seis bases dos sentidos como
condição, surge o contato.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, quem realiza o contato?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Essa não é
uma pergunta válida,” o Abençoado respondeu. “Eu não digo ‘alguém realiza o
contato.’ Se eu dissesse ‘alguém realiza o contato,’ então nesse caso esta
seria uma pergunta válida: ‘Venerável senhor, quem realiza o contato?’ Mas eu
não falo dessa forma. Visto que não falo dessa forma, se alguém me perguntar,
‘Venerável senhor, com o que como condição o contato [surge]?’ essa seria uma
pergunta válida. A resposta válida a essa pergunta é: ‘Com as seis bases como
condição, o contato [surge]; com o contato como condição, a sensação.’” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, quem sente?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Essa não é
uma pergunta válida,” o Abençoado respondeu. “Eu não digo ‘alguém sente.’ Se eu
dissesse ‘alguém sente,’ então nesse caso esta seria uma pergunta válida:
‘Venerável senhor, quem sente?’ Mas eu não falo dessa forma. Visto que não falo
dessa forma, se alguém me perguntar, ‘Venerável senhor, com o que como condição
a sensação [surge]?’ essa seria uma pergunta válida. A resposta válida a essa
pergunta é: ‘Com o contato como condição, a sensação [surge]; com a sensação
como condição, o desejo.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, quem deseja?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Essa não é
uma pergunta válida,” o Abençoado respondeu. “Eu não digo ‘alguém deseja.’ Se
eu dissesse ‘alguém deseja,’ então nesse caso esta seria uma pergunta válida:
‘Venerável senhor, quem deseja?’ Mas eu não falo dessa forma. Visto que não
falo dessa forma, se alguém me perguntar, ‘Venerável senhor, com o que como
condição o desejo [surge]?’ essa seria uma pergunta válida. A resposta válida a
essa pergunta é: ‘Com a sensação como condição, o desejo [surge]; com o desejo
como condição, o apego; com o apego como condição, o ser/existir. Essa é a
origem de toda essa massa de sofrimento.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas,
Phagguna, do desaparecimento e cessação sem deixar vestígios dessa mesma
ignorância cessam as formações volitivas. Da cessação das formações volitivas
cessa a consciência. Da cessação da consciência cessa a
mentalidade-materialidade (nome e forma). Da cessação da mentalidade-materialidade (nome e forma) cessam as
seis bases dos sentidos. Da cessação das seis bases dos sentidos cessa o
contato. Da cessação do contato cessa a sensação. Da cessação da sensação cessa
o desejo. Da cessação do desejo cessa o apego. Da cessação do apego cessa o
ser/existir. Da cessação do ser/existir cessa o nascimento. Da cessação do
nascimento, então o envelhecimento e morte, tristeza, lamentação, dor, angústia
e desespero, tudo cessa. Essa é a cessação de toda essa massa de sofrimento.”<b
style='mso-bidi-font-weight:normal'><o:p></o:p></b></span></p>


