<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
suponham que um feixe de cevada fosse colocado numa encruzilhada. Então viessem
seis homens com manguais nas mãos para malhar aquele feixe de cevada com os
seis manguais. Desse modo, aquele feixe de cevada ficaria bem malhado, depois
de ter sido malhado com aqueles seis manguais. Então, viesse um sétimo homem
com um mangual na mão e malhasse o feixe de cevada com o sétimo mangual. Desse
modo, aquele feixe de cevada ficaria malhado de forma ainda mais completa,
depois de ter sido malhado com o sétimo mangual. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Da mesma
maneira, bhikkhus, a pessoa comum sem instrução é malhada no olho pelas formas
agradáveis e desagradáveis; malhada no ouvido pelos sons agradáveis e desagradáveis;
malhada no nariz pelos aromas agradáveis e desagradáveis; malhada na língua
pelos sabores agradáveis e desagradáveis; malhada no corpo pelos tangíveis
agradáveis e desagradáveis; malhada na mente pelos objetos mentais agradáveis e desagradáveis. Se essa
pessoa comum sem instrução fixar a sua mente numa renovada existência futura,
então, essa pessoa estúpida será malhada de forma ainda mais completa, tal como
o feixe de cevada malhado com o sétimo mangual. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Certa vez
no passado, bhikkhus, os devas e os asuras estavam formados em ordem para a
batalha. Então Vepacitti, o senhor dos asuras, se dirigiu aos asuras da
seguinte forma: ‘Muito bem senhores, se nesta iminente batalha os asuras forem
vencedores e os devas forem derrotados, amarrem Sakka, o senhor dos devas, pelos quatro
membros e pelo pescoço e tragam-no até mim na cidade dos asuras.’ E Sakka, o
senhor dos devas, se dirigiu aos devas do Tavatimsa: ‘Muito bem senhores, se
nesta iminente batalha os devas forem vencedores e os asuras forem derrotados,
amarrem Vepacitti, o senhor dos asuras, pelos quatro membros e pelo pescoço e
tragam-no até mim no Sudhamma, o salão de assembléias dos devas.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Naquela
batalha os devas venceram e os asuras foram derrotados. Então os devas do
Tavatimsa amarraram Vepacitti pelos quatro membros e pelo pescoço e o levaram
até Sakka no Sudhamma, o salão de assembléias dos devas. E lá, Vepacitti, o
senhor dos asuras, ficou amarrado pelos quatro membros e pelo pescoço.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando
Vepacitti pensava: ‘Os devas são justos, os asuras são injustos; agora, aqui
mesmo, eu iria para a cidade dos devas,’ ele então se via livre dos grilhões em
volta dos membros e do pescoço desfrutando dos cinco elementos do prazer
sensual divino. Mas quando ele pensava: ‘Os asuras são justos, os devas são
injustos; agora eu irei para a cidade dos asuras,’ ele então se via atado pelos
quatro membros e pelo pescoço privado dos cinco elementos do prazer sensual
divino. <o:p></o:p></span></p>



<p class=Normal><a name=Mara></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>‘Tão sutis, bhikkhus, eram os grilhões de Vepacitti, porém ainda mais
sutis que isso são os grilhões de<span style='mso-spacerun:yes'>  </span>Mara.
Ao conceber, ele estará atado por Mara; ao não conceber, ele estará libertado
do Senhor do Mal. [<a href="#N1">1</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Bhikkhus, “Eu sou’ é uma concepção; ‘Eu sou isso’ é uma concepção; ‘Eu
serei’ é uma concepção; ‘Eu não serei’ é uma concepção; ‘Eu consistirei de
forma’ é uma concepção; ‘Eu serei desprovido de forma’ é uma concepção; ‘Eu
serei perceptivo’ é uma concepção; ‘Eu serei não-perceptivo’ é uma concepção;
‘Eu serei nem perceptivo, nem não-perceptivo’ é uma concepção. A concepção é uma
enfermidade, a concepção é um tumor, a concepção é uma flecha. Portanto,
bhikkhus, assim vocês deveriam treinar: ‘Permaneceremos com uma mente
desprovida de concepções.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
“Eu sou’ é uma perturbação; [<a href="#N2">2</a>] <a name=R2></a>‘Eu sou isso’
é uma perturbação; ‘Eu serei’ é uma perturbação ...<span
style='mso-spacerun:yes'>  </span>‘Eu serei nem perceptivo, nem não-perceptivo’
é uma perturbação. A perturbação é uma enfermidade, a perturbação é um tumor, a
perturbação é uma flecha. Portanto, bhikkhus, assim vocês deveriam treinar:
‘Permaneceremos com uma mente imperturbável.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
“Eu sou’ é uma vacilação; ‘Eu sou isso’ é uma vacilação; ‘Eu serei’ é uma
vacilação ...<span style='mso-spacerun:yes'>  </span>‘Eu serei nem perceptivo,
nem não-perceptivo’ é uma vacilação. A vacilação é uma enfermidade, a vacilação
é um tumor, a vacilação é uma flecha. Portanto, bhikkhus, assim vocês deveriam
treinar: ‘Permaneceremos com uma mente desprovida de vacilações.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
“Eu sou’ é uma proliferação; ‘Eu sou isso’ é uma proliferação; ‘Eu serei’ é uma
proliferação ... <span style='mso-spacerun:yes'> </span>‘Eu serei nem
perceptivo, nem não-perceptivo’ é uma proliferação. A proliferação é uma
enfermidade, a proliferação é um tumor, a proliferação é uma flecha. Portanto,
bhikkhus, assim vocês deveriam treinar: ‘Permaneceremos com uma mente
desprovida de proliferações.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
“Eu sou’ é uma conexão com a presunção; ‘Eu sou isso’ é uma conexão com a
presunção; ‘Eu serei’ é uma conexão com a presunção; ‘Eu não serei’ é uma
conexão com a presunção; ‘Eu consistirei de forma’ é uma conexão com a
presunção; ‘Eu serei desprovido de forma’ é uma conexão com a presunção; ‘Eu
serei perceptivo’ é uma conexão com a presunção; ‘Eu serei não-perceptivo’ é
uma conexão com a presunção; ‘Eu serei nem perceptivo, nem não-perceptivo’ é
uma conexão com a presunção. A presunção é uma enfermidade, a presunção é um
tumor, a presunção é uma flecha. Portanto, bhikkhus, assim vocês deveriam
treinar: ‘Permaneceremos com uma mente desprovida de presunção.’
Assim vocês devem treinar.”<o:p></o:p></span></p>

