<p class=Normal> “Bhikkhus, todos aqueles que têm inabalável convicção em relação a mim, entraram na correnteza. Daqueles que entraram na correnteza, cinco realizam o objetivo neste mundo, cinco realizam o objetivo tendo deixado este mundo.</p>


<p class=Normal> "Quem são os cinco que realizam o objetivo neste mundo? Aquele que logrou sete-vezes-no-máximo, aquele que logrou de família-a-família, aquele que logrou uma-semente, aquele que retorna uma vez, e aquele que nesta mesma vida, é um arahant. Esses cinco realizam o objetivo neste mundo. </p>


<p class=Normal> "Quem são os cinco que realizam o objetivo tendo deixado este mundo? Aquele que realiza nibbana no intervalo, aquele que realiza nibbana ao pousar, aquele que realiza nibbana sem esforço, aquele que realiza nibbana com esforço, e aquele destinado a um plano superior, o plano Akanittha. Esses cinco realizam o objetivo tendo deixado este mundo. </p>


<p class=Normal> "Todos esses, bhikkhus, que têm inabalável convicção em a mim, entraram na correnteza. Daqueles que realizam o entendimento correto, os primeiros cinco realizam o objetivo neste mundo, os últimos cinco realizam o objetivo tendo deixado este mundo."
