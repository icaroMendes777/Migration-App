<p class=Normal> "Bhikkhus, possuindo sete qualidades, um bhikkhu é um experto na disciplina. Quais sete?</p>

<p class=Normal> "(1) Ele sabe o que é uma transgressão. (2) Ele sabe o que não é uma transgressão. (3) Ele sabe o que é uma transgressão leve. (4) Ele sabe o que é uma transgressão grave. (5) Ele é virtuoso. Ele permanece contido de acordo com o Patimokkha, perfeito na conduta e na sua esfera de atividades. Temendo a menor falha, ele treina adotando os preceitos de virtude. (6) Ele é um daqueles que obtém de acordo com a sua vontade, sem problemas ou dificuldades, os quatro jhanas que constituem a mente superior e que proporcionam uma estada prazerosa aqui e agora. (7) Realizando por si mesmo através do conhecimento direto, aqui e agora, ele entra e permanece na libertação da mente e libertação através da sabedoria que são imaculadas com a destruição de todas as impurezas. </p>

<p class=Normal> "Possuindo essas sete qualidades, um bhikkhu é um experto na disciplina."</p>

