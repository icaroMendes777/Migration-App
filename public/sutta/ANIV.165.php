<p class=Normal style='margin-left:.75in'> 

<p class=Normal> “Bhikkhus, há quatro tipos de comportamento. Quais quatro? O tipo impaciente, o tipo paciente, o tipo domesticador e o tipo que alivia. </p>

<p class=Normal> “E qual, bhikkhus, é o tipo de comportamento impaciente? Se ele for criticado, ele critica em retorno; se ele for insultado, ele insulta em retorno; se ele for ofendido, ele ofende em retorno. </p>

<p class=Normal> “E qual, bhikkhus, é o tipo de comportamento paciente? Se ele for criticado, ele não critica em retorno; se ele for insultado, ele não insulta em retorno; se ele for ofendido, ele não ofende em retorno. </p>

<p class=Normal> “E qual, bhikkhus, é o tipo de comportamento domesticador? Neste caso, ao ver uma forma com o olho ... ao ouvir um som com o ouvido ... ao cheirar um aroma com o nariz … ao saborear um sabor com a língua … ao tocar um tangível com o corpo … ao conscientizar um objeto mental com a mente, um bhikkhu não se agarra aos seus sinais ou detalhes. Visto que, se permanecer com a faculdade da mente descuidada, ele será tomado pelos estados ruins e prejudiciais de cobiça e tristeza. Ele pratica a contenção, ele protege a faculdade da mente, ele se empenha na contenção da faculdade da mente. </p>

<p class=Normal> “E qual, bhikkhus, é o tipo de comportamento que alivia? Neste caso, um bhikkhu não tolera nele mesmo quaisquer pensamentos de desejo sensual, quaisquer pensamentos de má vontade, quaisquer pensamentos de crueldade, nem quaisquer outros estados ruins e prejudiciais que possam surgir nele. Ele os abandona, os dissipa, os elimina e os abole. </p>

<p class=Normal> “Esses são os quatro tipos de comportamento.” </p>

