<p class=Normal>Certa ocasião, o Abençoado estava caminhando na estrada entre Ukkattha e Setabya, e o brâmane Dona também estava caminhando na estrada entre Ukkattha e Setabya. O brâmane Dona viu as pegadas do Abençoado com rodas com mil raios e cubo, todas completas. Ao vê-las, ele pensou, “Que maravilhoso! Que admirável! Essas não são as pegadas de um ser humano!” </p>

<p class=Normal>Então, o Abençoado, saindo da estrada foi sentar ao pé de uma certa árvore, com as pernas cruzadas, mantendo o corpo ereto e estabelecendo a plena atenção à sua frente. Então Dona, seguindo as pegadas do Abençoado o viu sentado ao pé da árvore: confiante e inspirando confiança, com os sentidos em paz, com a mente em paz, tendo alcançado a tranqüilidade e controle supremos, treinado, protegido, as faculdades dos sentidos controladas, um naga. [<a href="#N1">1</a>] <a name="R1"></a>Ao vê-lo, Dona foi até ele e perguntou: </p>

<p class=Normal> “Mestre, você é um deva?” </p>

<p class=Normal> “Não, brâmane, eu não sou um deva.” </p>

<p class=Normal> “Você é um gandhabba?” </p>

<p class=Normal> “Não...” </p>

<p class=Normal> “... um yakkha?” </p>

<p class=Normal> “Não...” </p>

<p class=Normal> “... um ser humano?” </p>

<p class=Normal> “Não, brâmane, eu não sou um ser humano.” [<a href="#N2">2</a>] </p>

<p class=Normal><a name="R2"></a> “Quando perguntado ‘Você é um deva?’você responde, ‘Não, brâmane, eu não sou um deva.’ Quando perguntado ‘Você é um gandhabba?’ você responde, ‘Não, brâmane, eu não sou um gandhabba.’ Quando perguntado ‘Você é um yakkha?’ você responde, ‘Não, brâmane, eu não sou um yakkha.’ Quando perguntado ‘Você é um ser humano?’ você responde, ‘Não, brâmane, eu não sou um ser humano.’ Então que tipo de ser é você?” </p>

<p class=Normal> “Brâmane, aquelas impurezas através das quais – se elas não tivessem sido abandonadas – eu seria um deva: elas foram abandonadas por mim, cortadas pela raiz, feitas como com um tronco de palmeira eliminando-as de tal forma que não estarão mais sujeitas a um futuro surgimento. Aquelas impurezas através das quais – se elas não tivessem sido abandonadas – eu seria um gandhabba ... um yakkha ... um ser humano. Elas foram abandonadas por mim, cortadas pela raiz, feitas como com um tronco de palmeira eliminando-as de tal forma que não estarão mais sujeitas a um futuro surgimento. </p>

<p class=Normal> “Tal como uma flor de lótus vermelha, azul ou branca – que nasceu na água, cresceu na água, se eleva acima da água – permanece sem ser manchada pela água, da mesma forma eu – que nasci no mundo, cresci no mundo, tendo superado o mundo – vivo sem ser manchado pelo mundo. Lembre-se de mim, brâmane, como ‘iluminado.’ </p>

<p class=Normal style='margin-left:.75in'> “As impurezas através das quais eu iria<br> 
para o mundo dos devas, <br>
ou tornar-me-ia um gandhabba no céu, <br>  
ou iria para o mundo dos yakkhas, ou para o mundo humano: <br>
	&nbsp;&nbsp; &nbsp;&nbsp;essas foram destruídas em mim, <br>
	&nbsp;&nbsp; &nbsp;&nbsp;arruinadas, os seus caules removidos. <br>
Tal como uma flor de lótus azul, alçando-se, <br>
sem máculas da água, <br>
eu não sou maculado pelo mundo, <br>
portanto, brâmane, <br>
estou iluminado.”</p>

