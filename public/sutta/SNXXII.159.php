<p class=Normal> Em Savathi. Então, o venerável Ananda foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse: “Seria bom, venerável venerável senhor, se o Abençoado pudesse me ensinar o Dhamma de forma resumida, de modo que, tendo ouvido o Dhamma do Abençoado, eu possa permanecer só, isolado, diligente, ardente e decidido.” </p>

<p class=Normal> “O que você pensa, Ananda, a forma é permanente ou impermanente? </p>

<p class=Normal> “Impermanente, venerável senhor. </p>

<p class=Normal> "E aquilo que é impermanente é sofrimento ou felicidade? </p>

<p class=Normal> “Sofrimento, venerável senhor. </p>

<p class=Normal> “E é adequado considerar o que é impermanente, sofrimento, sujeito a mudanças como: ‘Isso é meu. Isso sou eu. Isso é o meu eu’? </p>

<p class=Normal> “Não, venerável senhor. </p>

<p class=Normal> “... a sensação é permanente ou impermanente? </p>

<p class=Normal> “Impermanente, venerável senhor. </p>

<p class=Normal> “... a percepção é permanente ou impermanente? </p>

<p class=Normal> “Impermanente, venerável senhor. </p>

<p class=Normal> “... as formações volitivas são permanentes ou impermanentes? </p>

<p class=Normal> “Impermanentes, venerável senhor. </p>

<p class=Normal> “O que você pensa, Ananda, a consciência é permanente ou impermanente? </p>

<p class=Normal> “Impermanente, venerável senhor. </p>

<p class=Normal> “E aquilo que é impermanente é sofrimento ou felicidade? </p>

<p class=Normal> “Sofrimento, venerável senhor. </p>

<p class=Normal> “E é adequado considerar o que é impermanente, sofrimento, sujeito a mudanças como: 'Isso é meu. Isso sou eu. Isso é o meu eu’? </p>

<p class=Normal> “Não, venerável senhor. </p>

<p class=Normal> “Portanto, Ananda, qualquer forma, quer seja do passado, futuro ou presente, interna ou externa; grosseira ou sutil; inferior ou superior, próxima ou distante: toda forma deve ser vista como na verdade é, com correta sabedoria: ‘Isso não é meu, isso não sou eu, isso não é o meu eu.’ </p>

<p class=Normal> “Qualquer sensação... </p>

<p class=Normal> “Qualquer percepção... </p>

<p class=Normal> “Quaisquer formações volitivas... </p>

<p class=Normal> “Qualquer consciência, quer seja do passado, do futuro ou do presente, interna ou externa; grosseira ou sutil; inferior ou superior; próxima ou distante: toda consciência deve ser vista como na verdade é, com correta sabedoria: ‘Isso não é meu, isso não sou eu, isso não é o meu eu.’ </p>

<p class=Normal> “Vendo dessa forma, o nobre discípulo bem instruído se desencanta com a forma, desencanta com a sensação, desencanta com a percepção, desencanta com as formações volitivas, desencanta com a consciência. Desencantado ele se torna desapegado. Através do desapego a sua mente é libertada. Quando ela está libertada surge o conhecimento: ‘Libertada.’ Ele compreende que: ‘O nascimento foi destruído, a vida santa foi vivida, o que deveria ser feito foi feito, não há mais vir a ser a nenhum estado.’” </p>


