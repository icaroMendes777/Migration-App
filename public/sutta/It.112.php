para tudo isso. Por isso ele é chamado o Tathagata. [<a href="#N2">2</a>]<o:p></o:p></span></p>


<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Desde a noite em que o Tathagata despertou completamente para a
insuperável perfeita iluminação até a noite na qual ele realize o <i
style='mso-bidi-font-style:normal'>parinibbana</i>, tudo aquilo que o Tathagata
disse, falou, explicou é assim (<i style='mso-bidi-font-style:normal'>tatha</i>)
e não de outra forma. Por isso ele é chamado o Tathagata.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O
Tathagata é aquele que age de acordo (<i style='mso-bidi-font-style:normal'>tatha</i>)
