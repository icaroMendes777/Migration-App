<span lang=PT-BR style='mso-ansi-language:
PT-BR'>mas aquele com a conduta e o conhecimento consumados<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>é o melhor dentre devas e humanos.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o
que disse o Abençoado. Vasettha and Bharadvaja ficaram satisfeitos e contentes
com as palavras do Abençoado.<o:p></o:p></span></p>



