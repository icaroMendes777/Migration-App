<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Esse verso
foi declamado da forma correta, não incorreta; dito com correção, não
incorreção; conectado com o que é benéfico, não desconectado. Eu também digo,
Vasettha:<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Os khattiya são os melhores dentre as pessoas<br>

<span lang=PT-BR style='mso-ansi-language:
