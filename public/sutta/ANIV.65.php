<p class=Normal> "Bhikkhus, há esses quatro tipos de pessoas que podem ser encontradas no mundo. Quais quatro? (1) Aquela que julga com base na forma, que confia com base na forma; (2) Aquela que julga com base na fala, que confia com base na fala; (3) Aquela que julga com base na austeridade, que confia com base na austeridade; (4) Aquela que julga com base no Dhamma, que confia com base no Dhamma. Esses são os quatro tipos de pessoas que podem ser encontradas no mundo.</p>

<p class=Normal>Aqueles que julgam com base na forma <br>
e aqueles que seguem devido à linguagem<br>
subjugaram-se ao controle do desejo e paixão; <br>
essas pessoas não compreendem. </p>

<p class=Normal>Quem não conhece o interior<br>
e não vê o exterior, <br>
um tolo obstruído de ambos lados, <br>
é fascinado pela linguagem.</p>

<p class=Normal>Quem não conhece o interior<br>
mas com clareza vê o exterior, <br>
vendo o resultado no exterior, <br>
também é fascinado pela linguagem.</p>

<p class=Normal>Mas quem compreende o interior<br>
e com clareza vê o exterior, <br>
vendo sem obstáculos, <br>
