<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>E o
Abençoado respondeu da mesma forma que antes.<o:p></o:p></span></p>



<p class=Normal><a name=T3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>3. Quando <span class=SpellE>Kevaddha</span> repetiu o seu pedido pela
terceira vez, o Abençoado disse: “<span class=SpellE>Kevaddha</span>, há três
tipos de maravilhas que eu declarei, tendo realizado por mim mesmo através do
conhecimento direto. Quais três? A maravilha dos poderes espirituais, [<a
href="#N1">1</a>] <a name=R1></a>a maravilha da <span class=GramE>telepatia,</span>[<a
href="#N2">2</a>] <a name=R2></a>a maravilha do ensinamento.[<a href="#N3">3</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>4. “Qual é a maravilha dos poderes espirituais? Neste caso, <span
class=SpellE>Kevaddha</span>, um bhikkhu exerce os vários tipos de poderes
supra-humanos: tendo sido um, ele se torna <span class=GramE>vários ...</span>
(igual ao <a href="DN2.php#T87">DN 2, verso 87</a>) ele exerce poderes
corporais até mesmo nos distantes mundos de Brahma. Então, alguém que tem fé e
confiança vê ele fazer esse tipo de coisas.<o:p></o:p></span></p>



<p class=Normal><a name=T5></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>5. “Ele conta aquilo para outro que é cético e descrente, dizendo: “É
maravilhoso, senhor, é admirável, o grande poder e habilidade daquele <span
class=GramE>contemplativo ...</span>” E aquele homem poderá dizer: “Senhor, há
algo chamado o encanto de <span class=SpellE>Gandhara</span>. [<a href="#N4">4</a>]
<a name=R4></a>É através disso que aquele bhikkhu se torna <span class=GramE>vários
...</span>” O que você pensa, <span class=SpellE>Kevaddha</span>, um cético não
diria isso para um crente?”<br> “Ele diria, venerável senhor.”<br> “É por isso, <span
class=SpellE>Kevaddha</span>, que vendo o perigo nesses feitos, eu não os
aprecio, eu os rejeito e desprezo.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. “E qual
é a maravilha da telepatia? Neste caso, <span class=SpellE>Kevaddha</span>, um
bhikkhu lê a mente de outros seres, outras pessoas, lê os seus estados mentais,
os seus pensamentos e ponderações, e diz: ‘Assim é a sua mente, assim é como
ela tende, assim é o seu <span class=GramE>coração.</span>’ Então, alguém que
tem fé e confiança vê ele fazer esse tipo de coisas.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. “Ele
conta aquilo para outro que é cético e descrente, dizendo: “É maravilhoso,
senhor, é admirável, o grande poder e habilidade daquele <span class=GramE>contemplativo
...</span>” E aquele homem poderá dizer: “Senhor, há algo chamado o encanto de <span
class=SpellE>Manika</span>. É através disso que aquele bhikkhu lê a mente de
outros <span class=GramE>seres ...</span>” O que você pensa, <span
class=SpellE>Kevaddha</span>, um cético não diria isso para um crente?”<br> “Ele
diria, venerável senhor.”<br> “É por isso, <span class=SpellE>Kevaddha</span>, que
vendo o perigo nesses feitos, eu não os aprecio, eu os rejeito e desprezo.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. “E qual
é a maravilha do ensinamento? Neste caso, <span class=SpellE>Kevaddha</span>,
um bhikkhu dá um ensinamento deste modo: ‘Considere deste modo, não considere
daquele, dirija a sua mente desse modo, não daquele, abandone aquilo, obtenha
isto e persevere nisso.” Isso, <span class=SpellE>Kevaddha</span>, é chamado <span
class=GramE>a</span> maravilha do ensinamento.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9-66.
“Outra vez, <span class=SpellE>Kevaddha</span>, um Tathagata surge no mundo,
um arahant , perfeitamente iluminado, consumado no verdadeiro conhecimento e
conduta, bem-aventurado, conhecedor dos mundos, um líder insuperável de pessoas
preparadas para serem treinadas, mestre de devas e humanos, desperto, sublime.
Ele declara - tendo realizado por si próprio com o conhecimento direto - este
mundo com os seus devas, maras e brahmas, esta população com seus contemplativos
e brâmanes, seus príncipes e povo. Ele ensina o Dhamma, com o significado e
fraseado corretos, que é admirável no início, admirável no meio, admirável no
final; e ele revela uma vida santa que é completamente perfeita e imaculada. Um
discípulo segue a vida santa e pratica a virtude (<a href="DN2.php#T41">DN 2,
versos 41-63</a>). Ele protege as portas das faculdades dos sentidos e alcança
os quatro jhanas (<a href="DN2.php#T64">DN 2, versos 64-82</a>); ele realiza
vários <span class=SpellE>insights</span> (<a href="DN2.php#T83">DN 2, versos
83 -84</a>); Ele penetra as Quatro Nobres Verdades, o caminho que conduz à
cessação das impurezas (<a href="DN2.php#T85">DN 2, versos 85-97</a>). Ele
compreende que ‘O nascimento foi destruído, a vida santa foi vivida, o que
deveria ser feito foi feito, não há mais vir a ser a nenhum <span class=GramE>estado.</span>’”
Isso, <span class=SpellE>Kevaddha</span>, é chamado a maravilha do ensinamento.<o:p></o:p></span></p>



<p class=Normal><span class=GramE><span lang=PT-BR style='mso-ansi-language:
PT-BR'>67 .</span></span><span lang=PT-BR style='mso-ansi-language:PT-BR'> “E, <span
class=SpellE>Kevaddha</span>, eu experimentei essas maravilhas através do meu
próprio conhecimento direto. Certa vez, <span class=SpellE>Kevaddha</span>,
este pensamento surgiu na mente de <span class=GramE>um certo</span> bhikkhu
nesta mesma comunidade de bhikkhus: ‘Onde esses quatro grandes elementos – o
elemento terra, o elemento água, o elemento fogo, o elemento ar – têm fim?’
Então, ele alcançou uma tal concentração da mente que, quando a sua mente
estava concentrada, o caminho que conduz aos devas apareceu na sua frente.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>68. “Então
chegando ao mundo dos devas dos Quatro Grandes Reis ele perguntou: ‘Amigos,
onde os quatro grandes elementos – o elemento terra, o elemento água, o
elemento fogo, o elemento ar – têm <span class=GramE>fim?</span>’ Quando isto
foi dito, os devas dos Quatro Grandes Reis responderam: ‘Bhikkhu, nós não
sabemos onde os quatro grandes elementos têm fim. Mas os Quatro Grandes Reis
são mais elevados e sublimes que nós, eles devem saber onde os quatro grandes
elementos têm fim.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>69. “Então,
aquele bhikkhu foi até os Quatro Grandes Reis e fez a mesma pergunta, e eles
responderam: ‘Nós não <span class=GramE>sabemos ...</span><span
style='mso-spacerun:yes'>  </span>Mas os devas do Trinta e Três que são mais
elevados e sublimes que nós, eles devem saber...’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>70. “Então,
aquele bhikkhu foi até os devas do Trinta e Três que responderam: ‘Nós não <span
class=GramE>sabemos ...</span><span style='mso-spacerun:yes'>  </span>Mas <span
class=SpellE>Sakka</span>, o senhor dos devas que é mais elevado e sublime que
nós, ele deve saber...’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>71. “Sakka,
o senhor dos devas respondeu: ‘Os devas de <span class=SpellE>Yama</span> devem
<span class=GramE>saber ...</span>”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>72. “Os
devas de <span class=SpellE>Yama</span> disseram: ‘<span class=SpellE>Suyama</span>,
o filho dos devas, deve <span class=GramE>saber ...</span>”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>73. “<span
class=SpellE>Suyama</span> disse: ‘Os devas de <span class=SpellE>Tusita</span>
devem <span class=GramE>saber ...</span>”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>74. “Os
devas de <span class=SpellE>Tusita</span> disseram: ‘<span class=SpellE>Santusita</span>,
o filho dos devas, deve <span class=GramE>saber ...</span>”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>75. “<span
class=SpellE>Santusita</span> disse: ‘Os devas de <span class=SpellE>Nimmanarati</span>
devem <span class=GramE>saber ...</span>” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>76. “Os
devas de <span class=SpellE>Nimmanarati</span> disseram: ‘<span class=SpellE>Sunimmita</span>,
o filho dos devas, deve <span class=GramE>saber ...</span>”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>77. “<span
class=SpellE>Sunimitta</span> disse: ‘Os devas de <span class=SpellE>Paranimmita-Vasavatti</span>
devem <span class=GramE>saber ...</span>” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>78. “Os
devas de <span class=SpellE>Paranimmita-Vasavatti</span> disseram: ‘<span
class=SpellE>Vasavatti</span>, o filho dos devas, deve <span class=GramE>saber
...</span>”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>79. “<span
class=SpellE>Vasavatti</span> disse: Os devas do cortejo de Brahma devem <span
class=GramE>saber ...</span>”<o:p></o:p></span></p>



<p class=Normal><a name=T80></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>80. “Então, aquele bhikkhu alcançou <span class=GramE>uma tal</span>
concentração da mente que, quando a sua mente estava concentrada, o caminho que
conduz aos devas do cortejo de Brahma apareceu na sua frente. Então, ele foi
até os devas do cortejo de Brahma e fez a mesma pergunta e eles responderam:
‘Nós também não sabemos ...<span style='mso-spacerun:yes'>  </span>Mas ali está
Brahma, o Grande Brahma, o Conquistador, o Não-conquistado, <span class=SpellE>Omnisciente</span>,
Todo Poderoso, Senhor, Deus e Criador, Soberano, Providência Divina, Pai de
todos aqueles que são e serão. Ele é maior e mais sublime que nós. Ele deve
saber onde os quatro grandes elementos têm fim.’<br> ‘Mas onde, amigos, está o
Grande Brahma agora?’<br><span style='mso-spacerun:yes'>  </span>‘Bhikkhu, nós
também não sabemos onde, como ou quando Brahma irá aparecer. Porém, quando os
sinais são vistos - uma luz aparece e surge um brilho – então, Brahma
aparecerá. Pois esses sinais são os presságios da aparição de Brahma.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>81. “Então,
não demorou muito para que Brahma aparecesse. E aquele bhikkhu foi até o Grande
Brahma e perguntou:<span style='mso-spacerun:yes'>  </span>‘Amigo, onde os
quatro grandes elementos – o elemento terra, o elemento água, o elemento fogo,
o elemento ar – têm <span class=GramE>fim?</span>’ Quando isto foi dito, o
Grande Brahma respondeu: ‘Bhikkhu, eu sou Brahma, o Grande Brahma, o
Conquistador, o Não-conquistado, <span class=SpellE>Omnisciente</span>, Todo
Poderoso, Senhor, Deus e Criador, Soberano, Providência Divina, Pai de todos
aqueles que são e serão.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>82. “Uma
segunda vez o bhikkhu disse para o Grande Brahma: ‘Amigo, eu não lhe perguntei
se você é Brahma, o Grande Brahma, o Conquistador, o Não-conquistado, <span
class=SpellE>Omnisciente</span>, Todo Poderoso, Senhor, Deus e Criador,
Soberano, Providência Divina, Pai de todos aqueles que são e serão. Eu
perguntei onde<span style='mso-spacerun:yes'>  </span>esses quatro grandes
elementos – o elemento terra, o elemento água, o elemento fogo, o elemento ar –
têm <span class=GramE>fim.</span>’ E uma segunda vez o Grande Brahma respondeu
da mesma forma ao bhikkhu. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>83. “Uma
terceira vez o bhikkhu disse: ‘Amigo, eu não lhe perguntei isso, eu perguntei
onde esses quatro grandes elementos – o elemento terra, o elemento água, o
elemento fogo, o elemento ar – têm <span class=GramE>fim.</span>’ Então, <span
class=SpellE>Kevaddha</span>, o Grande Brahma tomou o braço daquele bhikkhu e o
conduziu para um lado e disse: ‘Bhikkhu, esses devas acreditam que não há nada
que Brahma não veja, não há nada que ele não saiba, não há nada que ele não
tenha conhecimento. É por isso que não falei na presença deles. Mas bhikkhu, eu
não sei onde os quatro grandes elementos têm fim. Portanto, bhikkhu, você agiu
de modo incorreto ao passar pelo Abençoado em busca de uma resposta para essa
pergunta noutro lugar. Agora, bhikkhu, você deve ir até o Abençoado e fazer-lhe
essa pergunta e qualquer resposta que ele der, aceite-a.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>84. “Então,
da mesma forma como um homem forte pode estender o seu braço flexionado ou
flexionar o seu braço estendido o bhikkhu desapareceu do mundo de Brahma e
imediatamente apareceu na minha frente. Depois de me cumprimentar ele sentou a
um lado e disse: ‘Venerável senhor, onde esses quatro grandes elementos – o
elemento terra, o elemento água, o elemento fogo, o elemento ar – têm <span
class=GramE>fim?</span>’<o:p></o:p></span></p>



<p class=Normal><a name=T85></a><span lang=PT-BR style='mso-ansi-language:
