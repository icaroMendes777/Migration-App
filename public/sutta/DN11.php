<p class=Normal><a name=N4a></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[4a] O Dhammika Sutta (AN VI.54) traz um trecho idêntico a este. Andrew Olendzky, 
que é o diretor executivo do BCBS, fez a tradução desse sutta do Pali para o Inglês no Access to Insight, com a seguinte nota:</p>

<p class=Normal>'A palavra traduzida como “ido em definitivo” é <i>tathagatako</i>, e esta história pode ajudar a 
entender o epíteto “Tathagata” que o Buda com freqüência usava para descrever a si mesmo. </p>

<p class=Normal>O seu nome de nascimento era Siddhattha; quando adotou a vida de  contemplativo, ele era 
conhecido pelo nome do clã da sua mãe, Gotama; na Índia da época, ele era conhecido como o 
sábio do clã do seu pai, ou Sakyamuni; e depois da iluminação ele passou a ser conhecido 
como Buda, o Iluminado. Os seus discípulos com freqüência o chamavam  Bhagavant, ou “Abençoado,” 
mas o nome que ele mesmo quase sempre usava para si era Tathagata.</p>

<p class=Normal>Tathagata sempre foi uma palavra complicada para traduzir. A palavra <i>tatha</i> pode ser interpretada 
como “assim, tal como, dessa forma,” e <i>gata</i> é o particípio do verbo ir e simplesmente significa “ido.”  
Com freqüência encontramos a palavra traduzida para o inglês como “assim ido” ou “aquele que assim foi.” </p>

<p class=Normal>Confesso que nunca entendi realmente o significado do termo  Tathagata, até que me deparei com a 
história neste sutta. Com a imagem de um pássaro solto pelos marinheiros em busca de terra para 
pousar, um número de coisas começaram a fazer sentido.</p>

<p class=Normal>Para começar, devemos reconhecer os dois modos em que a expressão é usada: um, referindo-se ao 
Buda como um ser que não mais irá renascer e o outro, descrevendo como a consciência de uma pessoa 
iluminada, que ainda se encontra neste mundo, se relaciona como os objetos da experiência.</p>

<p class=Normal>Algumas vezes quando um dos arahants morria, Mara, sob a forma de uma nuvem escura, podia ser 
visto procurando onde a consciência havia se re-estabelecido, (isto é, renascido). Nesses casos, 
o Buda diz que com relação ao arahant a sua consciência não se re-estabelece em nenhum lugar 
(veja por exemplo o <a href="../sutta/SNIV.23.php#mara">SN IV.23</a>). Com esse sentido, o Buda está 
claramente empregando o termo  "Tathagata" com o significado como aquele que não irá renascer – tal qual 
o pássaro que deixa o navio sem retornar, a consciência dele não se estabelece novamente em nenhum 
dos mundos para novamente se juntar a um outro corpo.</p>

<p class=Normal>Mas há também um sentido no qual a frase descreve com habilidade a natureza de uma mente iluminada 
aqui nesta vida. Quando inquisidores tentavam fazer com que o Buda respondesse se a consciência dele 
sobreviveria após a morte, ele os rechaçava dizendo que mesmo no aqui e agora a consciência de um 
Tathagata não é detectável, visto que não há meios de medi-la ou percebê-la (<a href="../sutta/SnpV6.php#medir">Snp V.6</a>). É dito que 
a mente desperta não está apegada a nada no mundo – como um pássaro que não pousa, e desta forma 
não fica atada a nenhum objeto da experiência. </p>

<p class=Normal>Na verdade aprender a desapegar a mente dos seus grilhões compreende boa parte do treinamento da 
meditação de insight. O <a href="../sutta/MN10.php"> Satipatthana Sutta</a>, por exemplo, indica 
que ao praticar a atenção plena da forma correta, a pessoa “permanece independente, sem nenhum apego 
a qualquer coisa mundana.” O chefe de família Anathapindika, pouco antes de morrer, recebeu <a href="../sutta/MN143.php"> 
instruções de Sariputta </a>estimulando-o a que treinasse desta forma: “Eu não me apegarei ao que é visto, 
ouvido, sentido, conscientizado, buscado, procurado, ponderado pela mente e a minha consciência não dependerá disso.” </p>

<p class=Normal>Tudo isso junto sugere que um aspecto crucial do ensinamento do Buda é a noção da consciência desapegada 
dos objetos materiais ou mentais. Significa abandonar os apegos e que a cada momento as experiências sejam 
apenas o que são. Talvez com o treinamento adequado possamos viver como um pássaro que voa livre em círculos 
em torno do navio do nosso corpo e do nosso mundo, ao invés de aprisionado numa gaiola no convés.'  
</span>[<a
