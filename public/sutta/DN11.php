<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião, o Abençoado estava em <span class=SpellE>Nalanda</span>,
no manguezal de <span class=SpellE>Pavarika</span>. Então, o chefe de família <span
class=SpellE>Kevaddha</span> foi até o Abençoado e depois de cumprimentá-lo
sentou a um lado e disse: “Venerável senhor, esta <span class=SpellE>Nalanda</span>
é rica, próspera, com muitos <span class=GramE>habitantes e repleta de gente
que tem fé no Abençoado</span>. Seria bom se o Abençoado ordenasse que algum
bhikkhu realize feitos supra-humanos. Dessa forma <span class=SpellE>Nalanda</span>
passará a ser ainda mais devota do Abençoado.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O Abençoado
respondeu: “<span class=SpellE>Kevaddha</span>, não é dessa forma que eu ensino
o <span class=SpellE>Dharnma</span> aos bhikkhus, dizendo: ‘Vão, bhikkhus,
realizem feitos supra-humanos para os discípulos leigos vestidos de <span
class=GramE>branco!</span>’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. Uma segunda
vez <span class=SpellE>Kevaddha</span> disse: “Venerável senhor, não quero ser
importuno, mas ainda assim digo: ‘Esta <span class=SpellE>Nalanda</span> é
