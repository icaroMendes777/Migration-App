<p class=Normal>Assim ouvi. Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no Parque de Anathapindika. Agora, naquela ocasião, depois de haverem esmolado comida e de haverem retornado, após a refeição, um certo número de bhikkhus estavam reunidos no pavilhão quando surgiu esta conversa:</p>



<p class=Normal>"Agora, veneráveis amigos, qual destes dois reis possui a maior riqueza, as maiores posses, a maior força, o maior poder, mais majestade - o rei Seniya Bimbisara de Magadha, ou o Rei Pasenadi de Kosala?”</p>



<p class=Normal>E essa conversa dos bhikkhus continuou sem chegar a um fim.</p>



<p class=Normal>Então o Abençoado saindo do seu isolamento no final da tarde, dirigiu-se para o pavilhão e, chegando, sentou-se em um assento que havia sido preparado. Ele então perguntou aos bhikkhus: </p>



<p class=Normal>“Bhikkhus, qual é o assunto que faz com que vocês estejam sentados juntos aqui agora? E qual é a discussão que foi interrompida?”</p>



<p class=Normal>"Aqui, venerável senhor, "Depois de havermos esmolado comida e de havermos retornado, após a refeição, nos reunidos aqui no pavilhão quando surgiu esta conversa:</p>



<p class=Normal>"Agora, veneráveis amigos, qual destes dois reis possui a maior riqueza, as maiores posses, a maior força, o maior poder, mais majestade - o rei Seniya Bimbisara de Magadha, ou o Rei Pasenadi de Kosala?" Essa, venerável senhor foi a discussão que foi interrompida quando o Abençoado chegou.”</p>



<p class=Normal>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p>



	<p class=Normal style='margin-left:.75in'>"A felicidade decorrente dos prazeres sensuais, e a felicidade dos reinos celestiais, <br>

	não equivalem nem a um dezesseis avos da felicidade da cessação do desejo.”</p>


