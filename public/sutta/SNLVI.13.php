<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
há essas Quatro Nobres Verdades. Quais quatro? A nobre verdade do sofrimento, a
nobre verdade da origem do sofrimento, a nobre verdade da cessação do
sofrimento, a nobre verdade do caminho que conduz à cessação do sofrimento.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é a nobre verdade do sofrimento? Deve ser dito: os cinco agregados
influenciados pelo apego, isto é, o agregado da forma influenciado pelo apego,
o agregado da sensação influenciado pelo apego, o agregado da percepção influenciado pelo apego, o agregado das formações influenciado pelo apego, o agregado da consciência influenciado pelo apego. Isso é chamado a nobre
verdade do sofrimento.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é a nobre verdade da origem do sofrimento? É este desejo que conduz a
uma renovada existência,<span style='mso-spacerun:yes'>  </span>acompanhado
pela cobiça e pelo prazer, buscando o prazer aqui e ali; isto é, o desejo pelos
prazeres sensuais, o desejo por ser/existir,<span style='mso-spacerun:yes'> 
</span>o desejo por não ser/existir. Isso é chamado a nobre verdade da origem
do sofrimento<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é a nobre verdade da cessação do sofrimento? É o desaparecimento e cessação sem deixar vestígios daquele mesmo desejo, abrir mão, descartar, libertar-se, despegar desse mesmo desejo. Isso é chamado a nobre verdade da
cessação do sofrimento.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é a nobre verdade do caminho que conduz à cessação do sofrimento? É este
Nobre Caminho <a name=PVW>Óctuplo</a>: entendimento correto, pensamento
correto, palavra correta, ação correta, modo de vida correto, esforço correto,
atenção plena correta, concentração correta. . Isso é chamado a nobre verdade
do caminho que conduz à cessação do sofrimento<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Essas,
bhikkhus, são as Quatro Nobres Verdades. <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
bhikkhus, o esforço deve ser feito para compreender: ‘Isto é sofrimento.’ O
esforço deve ser feito para compreender: ‘Esta é a origem do sofrimento.’ O
esforço deve ser feito para compreender: ‘Esta é a cessação do sofrimento.’ O
esforço deve ser feito para compreender: ‘Este é o caminho que conduz à
cessação do sofrimento.’”<o:p></o:p></span></p>

