<p class=Normal>Assim ouvi. Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no Parque de Anathapindika. Naquela ocasião o Venerável Sangamaji havia chegado em Savatthi para ver o Abençoado, e aquela que outrora fora esposa do contemplativo Sangamaji ouviu dizer "O Mestre Sangamaji chegou em Savatthi." Então, levando consigo seu filho pequeno foi até o Bosque de Jeta.</p>



<p class=Normal>Então, ao chegar, ela foi até Venerável Sangamaji e disse "Cuide de mim, contemplativo, e deste pequeno filho também." Quando isto foi dito o Venerável Sangamaji permaneceu em silêncio. Uma segunda vez, ela disse ... Uma terceira vez, ela disse "Cuide de mim, contemplativo, e deste pequeno filho também." Quando isto foi dito, pela terceira vez, o Venerável Sangamaji permaneceu em silêncio.</p>



<p class=Normal>Então, o Venerável Sangamaji, não olhou para criança e tampouco direcionou ao menino uma palavra. Ao ver isso, a mulher pensou "Este contemplativo sequer se importa com o seu filho." Então, afastando-se e levando consigo a criança, a mulher partiu.</p>



<p class=Normal>O Abençoado então viu - através do olho divino, purificado e que sobrepuja o humano - a conduta indevida daquela que outrora fora esposa do contemplativo Sangamaji.</p>



<p class=Normal>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p>



	<p class=Normal style='margin-left:.75in'>"Com a sua chegada, não houve deleite;<br>

	com a sua partida, tampouco lamentação;<br>

	vitorioso, livre dos grilhões:<br>

	ele é aquele que eu chamo de brâmane.”</p>


