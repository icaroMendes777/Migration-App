<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Em certa
ocasião o Abençoado estava em Savatthi, no palácio da mãe de Migara, no Parque
do Oriente. <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. Naquela
ocasião – no décimo quinto dia do Uposatha, na noite de lua cheia - o Abençoado
estava sentado ao ar livre, rodeado pela Sangha dos bhikkhus. Então, observando
a silenciosa Sangha dos bhikkhus, ele assim falou: <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3.
“Bhikkhus, uma pessoa falsa [<a href="#N1">1</a>] <a name=R1></a>distingue uma
outra pessoa falsa? ‘Esta é uma pessoa falsa’?” – “Não, venerável senhor.” –
“Muito bem, bhikkhus. É impossível, não pode ser, que uma pessoa falsa possa
distinguir uma outra pessoa falsa: ‘Esta é uma pessoa falsa.’ Mas uma pessoa
falsa distinguiria uma pessoa verdadeira: ‘Esta é uma pessoa verdadeira’?” –
“Não, venerável senhor.” – “Muito bem, bhikkhus. É impossível, não pode ser,
que uma pessoa falsa possa distinguir uma pessoa verdadeira: ‘Esta é uma pessoa
verdadeira.’ <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4.
“Bhikkhus, um pessoa falsa possui qualidades ruins; ela se associa como uma
pessoa falsa, ela tem a volição como uma pessoa falsa, ela aconselha como uma
pessoa falsa, ela fala como uma pessoa falsa, ela age como uma pessoa falsa,
ela tem idéias como uma pessoa
falsa e ela dá oferendas como uma pessoa falsa. <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. “E como
uma pessoa falsa possui qualidades ruins? Neste caso uma pessoa falsa não tem
fé, nem vergonha e temor das transgressões; ela não aprendeu nada, é
preguiçosa, desatenta e tola. Assim é como uma pessoa falsa possui
qualidades ruins. <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. “E como
uma pessoa falsa se associa como uma pessoa falsa? Neste caso, uma pessoa falsa
tem como amigos e companheiros aqueles contemplativos e brâmanes que não
possuem fé, nem vergonha e temor das transgressões; que não aprenderam nada,
são preguiçosos, desatentos e tolos. Assim é como uma pessoa falsa se
associa como uma pessoa falsa. <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. “E como
uma pessoa falsa tem a volição como uma pessoa falsa? Neste caso, uma pessoa
falsa intenciona a sua própria aflição, intenciona a aflição dos outros e intenciona a aflição de
ambos. Assim é como uma pessoa falsa tem a volição como uma pessoa falsa.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. “E como
uma pessoa falsa aconselha como uma pessoa falsa? Neste caso, uma pessoa falsa
aconselha para a sua própria aflição, aconselha para a aflição dos outros e aconselha para a
aflição de ambos. Assim é como uma pessoa falsa aconselha como uma pessoa
falsa.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. “E como
uma pessoa falsa fala como uma pessoa falsa? Neste caso, uma pessoa falsa
emprega a linguagem mentirosa, emprega a linguagem maliciosa, emprega a linguagem rude e emprega a
linguagem frívola. Assim é como uma pessoa falsa fala como uma pessoa falsa.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. “E como
uma pessoa falsa age como uma pessoa falsa? Neste caso, uma pessoa falsa mata
seres vivos, toma aquilo que não é dado e tem conduta imprópria em relação aos
prazeres sensuais. Assim é como uma pessoa falsa age como uma pessoa falsa.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. “E como
uma pessoa falsa tem idéias como uma pessoa falsa? Neste caso, uma pessoa falsa
tem idéias como estas: ‘Não existe nada que é dado, nada<span
style="mso-spacerun: yes">  </span>que é oferecido, nada que é sacrificado; não
existe fruto ou resultado de ações boas ou más; não existe este mundo nem outro
mundo; não existe mãe, nem pai; nenhum ser que renasça espontaneamente; não
existem no mundo brâmanes nem contemplativos bons e virtuosos que, após terem
conhecido e compreendido diretamente por eles mesmos, proclamam este mundo e o
próximo.’ [<a href="#N2">2</a>] <a name=R2></a>Assim é como uma pessoa falsa tem idéias como uma pessoa falsa.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. “E como
uma pessoa falsa dá oferendas como uma pessoa falsa? Neste caso, uma pessoa
falsa dá uma oferenda de forma descuidada, não dá com as suas próprias mãos, dá
sem demonstrar respeito, dá aquilo que seria jogado fora, dá com a idéia de que
nenhum fruto provém dessa ação. Assim é como uma pessoa falsa dá oferendas como
uma pessoa falsa.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. “Essa
pessoa falsa que assim possui qualidades ruins, que assim se associa como uma
pessoa falsa, que tem a volição como uma pessoa falsa, que aconselha como uma
pessoa falsa, que fala como uma pessoa falsa, que age como uma pessoa falsa,
que tem idéias como uma pessoa falsa e que dá oferendas como uma pessoa falsa -
na dissolução do corpo após a morte, ela renasce na destinação das pessoas
falsas. E qual é o destino das pessoas falsas? É o inferno ou o reino animal.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14.
“Bhikkhus, uma pessoa verdadeira distingue uma outra pessoa verdadeira? ‘Esta é
uma pessoa verdadeira’?” – “Sim, venerável senhor.” – “Muito bem, bhikkhus. É
possível que uma pessoa verdadeira possa distinguir uma outra pessoa
verdadeira: ‘Esta é uma pessoa verdadeira.’ Mas uma pessoa verdadeira
distinguiria uma pessoa falsa: ‘Esta é uma pessoa falsa’?” – “Sim, venerável
senhor.” – “Muito bem, bhikkhus. É possível que uma pessoa verdadeira possa
distinguir uma pessoa falsa: ‘Esta é uma pessoa falsa.’<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15.
“Bhikkhus, uma pessoa verdadeira possui qualidades boas; ela se associa como
uma pessoa verdadeira, ela tem a volição como uma pessoa verdadeira, ela
aconselha como uma pessoa verdadeira, ela fala como uma pessoa verdadeira, ela
age como uma pessoa verdadeira, ela tem idéias como uma pessoa verdadeira e ela
dá oferendas como uma pessoa verdadeira.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16. “E como
uma pessoa verdadeira possui boas qualidades? Neste caso, uma pessoa verdadeira
tem fé, vergonha e temor das transgressões; ela é instruída, é energética,
plenamente atenta e sábia. Assim é como uma pessoa verdadeira possui boas
qualidades.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17. “E como
uma pessoa verdadeira se associa como uma pessoa verdadeira? Neste caso, uma
pessoa verdadeira tem como amigos e companheiros aqueles contemplativos e
brâmanes que possuem fé, vergonha e temor das transgressões; que são instruídos,
são energéticos, plenamente atentos e sábios. Assim é como uma pessoa
verdadeira se associa como uma pessoa verdadeira<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18. “E como
uma pessoa verdadeira tem a volição como uma pessoa verdadeira? Neste caso uma
pessoa verdadeira não intenciona a sua própria aflição, não intenciona a aflição dos
outros e não intenciona a aflição de ambos. Assim é como uma pessoa verdadeira tem a
volição como uma pessoa verdadeira.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>19. “E como
uma pessoa verdadeira aconselha como uma pessoa verdadeira? Neste caso uma
pessoa verdadeira não aconselha para a sua própria aflição, não aconselha para a aflição dos
outros e não aconselha para a aflição de ambos. Assim é como uma pessoa verdadeira aconselha
como uma pessoa verdadeira.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>20. “E como
uma pessoa verdadeira fala como uma pessoa verdadeira? Neste caso uma pessoa
verdadeira se abstém da linguagem mentirosa, se abstém da linguagem maliciosa, se abstém da linguagem
rude e se abstém da linguagem frívola. Assim é como uma pessoa verdadeira fala como uma
pessoa verdadeira.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>21. “E como
uma pessoa verdadeira age como uma pessoa verdadeira? Neste caso uma pessoa
verdadeira se abstém de matar seres vivos, se abstém de tomar aquilo que não é dado e se abstém da
conduta imprópria em relação aos prazeres sensuais. Assim é como uma pessoa
verdadeira age como uma pessoa verdadeira<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>22. “E como
uma pessoa verdadeira tem idéias como uma pessoa verdadeira? Neste caso uma
pessoa verdadeira tem idéias como estas: ‘Existe aquilo que é dado e o que é
oferecido e o que é sacrificado; existe fruto e resultado de boas e más ações;
existe este mundo e o outro mundo; existe a mãe e o pai; existem seres que
renascem espontaneamente; existem no mundo brâmanes e contemplativos bons e
virtuosos que, após terem conhecido e compreendido diretamente por eles mesmos,
proclamam este mundo e o próximo.’ Assim é como uma pessoa verdadeira tem
idéias como uma pessoa verdadeira.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>23. “E como
uma pessoa verdadeira dá oferendas como uma pessoa verdadeira? Neste caso uma
pessoa verdadeira dá uma oferenda de forma cuidadosa, dá com as suas próprias
mãos, dá demonstrando respeito, dá uma oferenda valiosa, dá com a idéia de que
algum fruto provém dessa ação. Assim é como uma pessoa verdadeira dá oferendas
como uma pessoa verdadeira.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>24. “Essa
pessoa verdadeira que assim possui qualidades boas, que assim se associa como
uma pessoa verdadeira, que tem a volição como uma pessoa verdadeira, que
aconselha como uma pessoa verdadeira, que fala como uma pessoa verdadeira, que
age como uma pessoa verdadeira, que tem idéias como uma pessoa verdadeira e que
dá oferendas como uma pessoa verdadeira - na dissolução do corpo após a morte,
ela renasce na destinação das pessoas verdadeiras. E qual é o destino das
pessoas verdadeiras? É a magnitude entre os devas ou a magnitude entre os
seres humanos.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o que disse o
Abençoado. Os bhikkhus ficaram satisfeitos e contentes com as palavras do
Abençoado.</span></p>


