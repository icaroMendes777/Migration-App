<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quem adota
certas idéias, considerando-as como melhores e fazendo-as supremas no mundo,
devido a isso, ele diz que todas as outras idéias são inferiores; como
conseqüência ele não está livre de disputas (com os outros). <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ao ver
benefício para si mesmo nas coisas vistas, ouvidas, sentidas e nos preceitos e
rituais, agarrando-se apenas nisso com paixão, ele considera todo o restante
como inferior. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqueles habilidosos
(em julgar) [<a href="#N1">1</a>] <a name=R1></a>dizem que (uma idéia se torna)
um grilhão se, apoiando-se nela, alguém considera todo o demais como inferior.
Por conseguinte, um bhikkhu não deve estar sujeito ao que é visto, ouvido ou
sentido, aos preceitos e rituais. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Um bhikkhu
não conjura idéias sobre o mundo através do conhecimento ou preceitos e
rituais. Portanto, ele não considera a si mesmo ‘superior’, ‘inferior’, ou
‘igual’. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O sábio
abandonou a idéia de uma identidade e está livre do apego. Ele não depende nem
mesmo do conhecimento. Ele não toma partido nas disputas. Ele de forma nenhuma
se refugia numa idéia. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Nele não
há inclinação por qualquer extremo, por ser/existir ou não ser/existir, aqui ou
além. Ele não mais adota dogmas pois não necessita do consolo que os dogmas
oferecem. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O sábio
não tem idéias preconcebidas com relação ao que é visto, ao que é ouvido e ao
que é sentido. Como poderia alguém no mundo através do pensamento classificar
esse brâmane [<a href="#N2">2</a>] <a name=R2></a>que não se agarra a nenhuma
idéia?<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O Brâmane
não adota um certo dogma nem têm preferências. Ele não aprecia as idéias
dogmáticas e não é governado pelos preceitos ou rituais. Assim, o brâmane
cruzou para a outra margem, para nunca mais regressar.” <o:p></o:p></span></p>

