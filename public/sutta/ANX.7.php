<p class=Normal>Então o venerável Ananda foi até o venerável Sariputta e depois de cumprimentá-lo sentou a um lado e disse: “Amigo Sariputta, um bhikkhu poderia alcançar uma tal concentração da mente de modo que ele não teria a percepção da terra em relação à terra, nem da água em relação à água, nem do fogo … do ar … da base do espaço infinito ... da base da consciência infinita ... da base do nada ... da base da nem percepção, nem não percepção ... deste mundo ... nem do mundo além, e ainda assim seria perceptivo?” </p>

<p class=Normal> “Sim, amigo Ananda, um bhikkhu poderia …”</p>

<p class=Normal> “Mas como, amigo Sariputta, um bhikkhu poderia alcançar uma tal concentração  da mente de modo que ele não teria a percepção da terra em relação à terra ... nem do mundo além, e ainda assim seria perceptivo?” </p>

<p class=Normal> “Certa vez, amigo Ananda, estando aqui mesmo em Savatthi no Bosque do Homem Cego, eu alcancei uma tal concentração da mente de modo que não tinha a percepção da terra em relação à terra ... nem do mundo além, e ainda assim eu estava perceptivo.” </p>

<p class=Normal> “Mas com relação a quê, amigo Sariputta, você estava perceptivo naquela ocasião?” </p>

<p class=Normal> “’Nibbana é a cessação do ser/existir, nibbana é a cessação do ser/existir’: essa percepção surgiu em mim, amigo Ananda, na medida em que outra percepção cessava. Tal como numa fogueira queimando lenha, uma chama surge na medida em que outra chama cessa, assim também, ‘Nibbana é a cessação do ser/existir, nibbana é a cessação do ser/existir’: essa percepção surgiu em mim na medida em que outra cessava. Eu estava perceptivo naquela ocasião que ‘nibbana é a cessação do ser/existir.’” </p>

