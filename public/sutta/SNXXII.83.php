<p class=Normal> Em Savatthi. Então o Venerável Ananda se dirigiu aos bhikkhus: "Amigos bhikkhus!" </p>

<p class=Normal> "Amigo" aqueles bhikkhus responderam. O venerável Ananda disse o seguinte: </p>

<p class=Normal> "Amigos, o Venerável Punna Mantaniputta foi de grande ajuda quando estávamos recém-ordenados. Ele nos exortava da seguinte maneira: </p>

<p class=Normal> "É através do apego, Ananda, que [a noção] 'Eu sou' ocorre, não sem apego. E com apego em relação a que ocorre 'eu sou'? É através do apego à forma que 'eu sou' ocorre, não sem apego. É através do apego à sensação ... percepção ... formações volitivas ... consciência que 'eu sou' ocorre, não sem apego. </p>

<p class=Normal> "Suponha, Ananda, que uma jovem mulher - ou homem - vigorosos, que apreciam ornamentos, examinariam a própria face num espelho ou numa tigela com água limpa, pura, cristalina: ela olharia com apego, não sem apego. Da mesma forma, é através do apego que [a noção] 'Eu sou' ocorre, não sem apego. É através do apego à forma que 'eu sou' ocorre, não sem apego. É através do apego à sensação ... percepção ... formações volitivas ... consciência que 'eu sou' ocorre, não sem apego. </p>

<p class=Normal> "O que você pensa, Ananda, a forma é permanente ou impermanente “Impermanente, venerável senhor.” – “Aquilo que é impermanente é sofrimento ou felicidade?” – “Sofrimento, venerável senhor.” – “É adequado que aquilo que é impermanente, sofrimento e sujeito à mudança, seja considerado desta forma: ‘Isso é meu, isso sou eu, isso é o meu eu’?” – “Não venerável senhor.” </p>

<p class=Normal> “Ananda, o que você pensa: A sensação ... percepção ... formações volitivas ... consciência são permanentes ou impermanentes?” – “Impermanentes venerável senhor.” - “Aquilo que é impermanente é sofrimento ou felicidade?” – “Sofrimento, venerável senhor.” – “É adequado que aquilo que é impermanente, sofrimento e sujeito à mudança, seja considerado desta forma: ‘Isso é meu, isso sou eu, isso é o meu eu’?” – “Não venerável senhor.” </p>

<p class=Normal> Portanto, Ananda, qualquer tipo de forma material, quer seja do passado, do futuro ou do presente ... toda forma material deve ser vista como na verdade ela é, com correta sabedoria, deste modo: ‘Isso não é meu, isso não sou eu, isso não é o meu eu’. Qualquer tipo de sensação ... Qualquer tipo de percepção ... Qualquer tipo de formações volitivas ... Qualquer tipo de consciência ... toda consciência deve ser vista como na verdade ela é, com correta sabedoria, deste modo: ‘Isso não é meu, isso não sou eu, isso não é o meu eu.’ </p>

<p class=Normal> “Vendo desse modo um nobre discípulo bem instruído se desencanta com a forma material, se desencanta com a sensação, se desencanta com a percepção, se desencanta com as formações volitivas, se desencanta com a consciência. </p>

<p class=Normal> “Desencantado, ele se torna desapegado. Através do desapego a sua mente é libertada. Quando ela está libertada surge o conhecimento: ‘Libertada.’ Ele compreende que: ‘O nascimento foi destruído, a vida santa foi vivida, o que deveria ser feito foi feito, não há mais vir a ser a nenhum estado’. </p>

<p class=Normal> "Amigos, o venerável Punna Mantaniputta foi de grande ajuda quando estávamos recém-ordenados. Ele nos exortava dessa maneira. E ao ouvir esse ensinamento eu penetrei o Dhamma." </p>


