<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, há esses cinco primeiros grilhões.[<a href="#N1">1</a>] <a
name=R1></a>Quais cinco? A idéia da existência de um eu, a dúvida, o apego a
preceitos e rituais, desejo sensual e a má vontade. Esses são os cinco
primeiros grilhões. Os quatro fundamentos da atenção plena devem ser
desenvolvidos para o conhecimento direto desses cinco primeiros grilhões, para
a sua completa compreensão, para a sua total destruição e para o seu
abandono.”[<a href="#N2">2</a>]<o:p></o:p></span></p>

