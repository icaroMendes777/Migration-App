<p class=Normal>Assim ouvi. Em certa ocasião o Abençoado estava em Savathi, no Bosque de Jeta, no Parque de Anathapindika. Lá ele se dirigiu aos monges desta forma: "Bhikkhus." – "Venerável Senhor," eles responderam. O Abençoado disse o seguinte: </p>

<p class=Normal>"Bhikkhus, possuindo seis qualidades, um bhikkhu é merecedor de dádivas, merecedor de hospitalidade, merecedor de oferendas, merecedor de saudações com reverência, um campo inigualável de mérito para o mundo. Quais seis? (1) Aqui, tendo visto uma forma com o olho, um bhikkhu não fica alegre e tampouco triste, mas permanece equânime, com atenção plena, ardente e plenamente consciente. (2) Tendo ouvido um som com o ouvido, um bhikkhu não fica alegre e tampouco triste, mas permanece equânime, com atenção plena, ardente e plenamente consciente. (3) Tendo cheirado um aroma com o nariz, um bhikkhu não fica alegre e tampouco triste, mas permanece equânime, com atenção plena, ardente e plenamente consciente. (4) Tendo saboreado um sabor com a língua, um bhikkhu não fica alegre e tampouco triste, mas permanece equânime, com atenção plena, ardente e plenamente consciente. (5) Tendo tocado um tangível com o corpo, um bhikkhu não fica alegre e tampouco triste, mas permanece equânime, com atenção plena, ardente e plenamente consciente. (6) Tendo conscientizado um objeto mental com a mente, um bhikkhu não fica alegre e tampouco triste, mas permanece equânime, com atenção plena, ardente e plenamente consciente. Possuindo essas seis qualidades, um bhikkhu é merecedor de dádivas, merecedor de hospitalidade, merecedor de oferendas, merecedor de saudações com reverência, um campo inigualável de mérito para o mundo." </p>

<p class=Normal>Isso foi o que o Abençoado disse. Os bhikkhus ficaram satisfeitos e contentes com as palavras do Abençoado. </p>


