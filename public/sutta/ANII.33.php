<p class=Normal>"Bhikkhus, há duas pessoas que não podem facilmente ser retribuídas. Quais duas? A própria mãe e o próprio pai.</p>

<p class=Normal>"Mesmo que alguém carregue a mãe em um ombro e o pai no outro, e [que ao fazê-lo] tenha um tempo de vida de cem anos, viva por cem anos; que ele cuide deles, ungindo-os com bálsamos, massageando-os, dando-lhes banho, e esfregando os seus membros, e até mesmo limpando a sua urina e o seu excremento,  ainda assim, ele não teria feito o suficiente para os seus pais, nem lhes teria retribuído. Mesmo que ele estabeleça os seus pais como os supremos senhores e governantes sobre esta grande terra abundante com os sete tesouros, ele ainda assim não teria feito o suficiente para os seus pais, nem lhes teria retribuído. Por que razão? Os pais são de grande ajuda para os seus filhos, pois eles os criam, os alimentam e lhes mostram o mundo.</p>

<p class=Normal>"Mas, bhikkhus, se os pais não têm fé, ele os incentiva, os assenta, e os estabelece na fé; se os pais forem imorais, ele os incentiva, os assenta, e os estabelece no comportamento virtuoso; se os pais são avarentos, ele os incentiva, os assenta, e os estabelece na generosidade; se  os pais não são sábios, ele os incentiva, os assenta, e os estabelece na sabedoria: de tal modo que ele tenha feito o suficiente pelos seus pais, retribuindo-lhes, e fazendo mais do que suficiente por eles."</p>


