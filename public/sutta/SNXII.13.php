<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, aqueles contemplativos ou brâmanes que não compreendem o
envelhecimento e morte, a sua origem, a sua cessação e o caminho que conduz à
sua cessação; que não compreendem o nascimento ... ser/existir ... apego ...
desejo ... sensação ... contato ... seis bases dos sentidos ....
mentalidade-materialidade (nome e forma) ... consciência ... formações volitivas, a sua origem, a sua cessação
