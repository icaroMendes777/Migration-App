dentre contemplativos ou brâmanes dentre brâmanes, e esses veneráveis não irão,
realizando por si mesmos através do conhecimento direto, nesta mesma vida,
entrar e permanecer no objetivo da vida contemplativa ou no objetivo da vida
brâmane.<o:p></o:p></span></p>



