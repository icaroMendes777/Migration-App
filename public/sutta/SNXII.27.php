<p class=Normal> Em Savatthi. "Bhikkhus, com a ignorância como condição, as formações volitivas [surgem]; com as formações volitivas como condição, a consciência ...  mentalidade-materialidade (nome e forma) ... seis bases dos sentidos ... contato ... sensação ... desejo ... apego ... ser/existir  ... nascimento ... envelhecimento e morte, essa é a origem de toda essa massa de sofrimento. </p>

<p class=Normal>"O que, bhikkhus, é <i>envelhecimento e morte</i>? O envelhecimento dos seres nas diversas classes de seres, a sua idade avançada, os dentes quebradiços, os cabelos grisalhos, a pele enrugada, o declínio da vida, o enfraquecimento das faculdades - a isto se chama envelhecimento. O falecimento dos seres nas várias classes de seres, a sua morte, a dissolução, o desaparecimento, o morrer, a finalização do tempo, a dissolução dos agregados, o cadáver descartado - a isto se denomina morte.  Com o surgimento do nascimento surge o envelhecimento e morte; com a cessação do nascimento cessa o envelhecimento e morte. Justamente este nobre caminho óctuplo é o caminho que conduz à cessação do envelhecimento e morte: isto é, entendimento correto, pensamento correto, linguagem correta, ação correta, modo de vida correto, esforço correto, atenção plena correta, concentração correta. </p>

<p class=Normal>"O que, bhikkhus, é <i>nascimento</i>? O
nascimento dos seres nas várias classes de seres, o próximo nascimento, a
precipitação [em um ventre], a geração, a manifestação dos agregados, a
obtenção das bases para contato - a isto se denomina nascimento. Com o surgimento do ser/existir surge o nascimento; com a cessação do ser/existir cessa o nascimento. Justamente este nobre caminho óctuplo é o caminho que conduz à cessação do nascimento: isto é, entendimento correto, pensamento correto, linguagem correta, ação correta, modo de vida correto, esforço correto, atenção plena correta, concentração correta. </p>

<p class=Normal> &quot;E o que é <i style='mso-bidi-font-style:normal'>ser/existir? </i>Existem
esses três tipos de seres: seres do reino sensual, seres do reino da matéria sutil, (ou com forma), e seres do reino imaterial, (ou sem forma) - a isto se denomina ser/existir. Com o surgimento do ser/existir surge o nascimento; com a cessação do ser/existir cessa o nascimento. Justamente este nobre caminho óctuplo é o caminho que conduz à cessação do ser/existir: isto é, entendimento correto, pensamento correto, linguagem correta, ação  correta, modo de vida correto, esforço correto, atenção plena correta, concentração correta. </p>

<p class=Normal> &quot;O que é<span style="mso-spacerun: yes">  </span><i
style='mso-bidi-font-style:normal'>apego? </i>Existem esses quatro tipos de
apego: apego a prazeres sensuais, apego a idéias, apego a preceitos e rituais e
apego à idéia da existência de um eu - a isto se denomina apego. Com o surgimento do apego surge o ser/existir; com a cessação do apego cessa o ser/existir. Justamente este nobre caminho óctuplo é o caminho que conduz à cessação do apego: isto é, entendimento correto, pensamento correto, linguagem correta, ação  correta, modo de vida correto, esforço correto, atenção plena correta, concentração correta. </p>

<p class=Normal>&quot;O que é <i style='mso-bidi-font-style:normal'>desejo? </i>Existem
essas seis classes de desejo: desejo por formas, desejo por sons, desejo por
aromas, desejo por sabores, desejo por tangíveis, desejo por objetos mentais - a isto se denomina desejo. Com o surgimento do desejo surge o apego; com a cessação do desejo cessa o apego. Justamente este nobre caminho óctuplo é o caminho que conduz à cessação do desejo: isto é, entendimento correto, pensamento correto, linguagem correta, ação  correta, modo de vida correto, esforço correto, atenção plena correta, concentração correta. </p>


<p class=Normal> &quot;O que é<span style="mso-spacerun: yes">  </span><i
style='mso-bidi-font-style:normal'>sensação</i>? Existem essas seis classes de
sensações: sensações que surgem do contato no olho, sensações que surgem do
contato no ouvido, sensações que surgem do contato no nariz, sensações que
surgem do contato na língua, sensações que surgem do contato no corpo,
sensações que surgem do contato na mente - a isto se denomina sensação. Com o surgimento da sensação surge o desejo; com a cessação da sensação cessa o desejo. Justamente este nobre caminho óctuplo é o caminho que conduz à cessação da sensação: isto é, entendimento correto, pensamento correto, linguagem correta, ação  correta, modo de vida correto, esforço correto, atenção plena correta, concentração correta. </p>

<p class=Normal> &quot;O que é<span style="mso-spacerun: yes">  </span><i
style='mso-bidi-font-style:normal'>contato? </i>Existem essas seis classes de
contato: contato no olho, contato no ouvido, contato no nariz, contato na
língua, contato no corpo, contato na mente - a isto se denomina contato. Com o surgimento do contato surge a sensação; com a cessação do contato cessa a sensação. Justamente este nobre caminho óctuplo é o caminho que conduz à cessação do contato: isto é, entendimento correto, pensamento correto, linguagem correta, ação  correta, modo de vida correto, esforço correto, atenção plena correta, concentração correta. </p>

<p class=Normal> &quot;O que são <i>a</i><i style='mso-bidi-font-style:normal'>s seis
bases dos sentidos? </i>Existem essas seis bases: a base do olho, a base do
ouvido, a base do nariz, a base da língua, a base do corpo, a base da mente - a isto se denomina seis bases dos sentidos. Com o surgimento das seis
bases dos sentidos surge o contato; com a cessação das seis
bases dos sentidos cessa o contato. Justamente este nobre caminho óctuplo é o caminho que conduz à cessação das seis
bases dos sentidos: isto é, entendimento correto, pensamento correto, linguagem correta, ação  correta, modo de vida correto, esforço correto, atenção plena correta, concentração correta.</p>

<p class=Normal> &quot;O que é<span style="mso-spacerun: yes">  </span><i>mentalidade-materialidade (nome e forma)</i><i
style='mso-bidi-font-style:normal'>?</i> Sensação, percepção, volição, contato
e atenção - esses são chamados de mentalidade (nome). Os quatro grandes elementos e a
forma material derivada dos quatro grandes elementos - esses são chamados de
materialidade (forma) - a isto se denomina mentalidade-materialidade (nome e forma). Com o surgimento da mentalidade-materialidade (nome e forma) surgem as seis
bases dos sentidos; com a cessação da mentalidade-materialidade (nome e forma) cessam as seis
bases dos sentidos. Justamente este nobre caminho óctuplo é o caminho que conduz à cessação da mentalidade-materialidade (nome e forma): isto é, entendimento correto, pensamento correto, linguagem correta, ação  correta, modo de vida correto, esforço correto, atenção plena correta, concentração correta.</p>

<p class=Normal> &quot;O que é<span style="mso-spacerun: yes">  </span><i
style='mso-bidi-font-style:normal'>consciência? </i>Existem essas seis classes
de consciência: consciência no olho, consciência no ouvido, consciência no
nariz, consciência na língua, consciência no corpo, consciência na mente - a isto se denomina consciência. Com o surgimento da consciência surge a mentalidade-materialidade (nome e forma); com a cessação da consciência cessa a mentalidade-materialidade (nome e forma). Justamente este nobre caminho óctuplo é o caminho que conduz à cessação da consciência: isto é, entendimento correto, pensamento correto, linguagem correta, ação  correta, modo de vida correto, esforço correto, atenção plena correta, concentração correta.</p>

<p class=Normal> &quot;O que são<span style="mso-spacerun: yes">  </span><i
style='mso-bidi-font-style:normal'>formações volitivas? </i>Existem esses três tipos de
formações: a formação corporal, a formação verbal e a formação mental - a isto se denomina formações volitivas. Com o surgimento das formações volitivas surge a consciência; com a cessação das formações volitivas cessa a consciência. Justamente este nobre caminho óctuplo é o caminho que conduz à cessação das formações volitivas: isto é, entendimento correto, pensamento correto, linguagem correta, ação correta, modo de vida correto, esforço correto, atenção plena correta, concentração correta. </p>

<p class=Normal> &quot;O que é <i style='mso-bidi-font-style:normal'>ignorância? </i>Não
ter o conhecimento do sofrimento, não ter o conhecimento da origem do
sofrimento, não ter o conhecimento da cessação do sofrimento, não ter o
conhecimento do caminho que conduz à cessação do sofrimento - a isto se
denomina ignorância. Com o surgimento da ignorância surgem as formações volitivas; com a cessação da ignorância cessam as formações volitivas. Justamente este nobre caminho óctuplo é o caminho que conduz à cessação da ignorância: isto é, entendimento correto, pensamento correto, linguagem correta, ação correta, modo de vida correto, esforço correto, atenção plena correta, concentração correta. </p>

<p class=Normal>"Quando, bhikkhu, um nobre discípulo assim compreende a condição; assim compreende a origem da condição; assim compreende a cessação da condição; assim compreende o caminho que conduz à cessação da condição, ele é chamado um nobre discípulo com o entendimento perfeito, com a visão perfeita; ele realizou o verdadeiro Dhamma, ele vê esse verdadeiro Dhamma, ele possui o conhecimento de um treinando, que entrou na correnteza do Dhamma, um nobre com a sabedoria penetrante que está às portas do Imortal.” </p>

