<p class=Normal> “Bhikkhus, há essas três causas para a origem de kamma. Quais três? A cobiça é uma causa para a origem de kamma, a raiva é uma causa para a origem de kamma, e a delusão é uma causa para a origem de kamma.</p>

<p class=Normal> (1) "Qualquer kamma criado através da cobiça, nascido da cobiça, causado pela cobiça, originado da cobiça, esse kamma é prejudicial e repreensível e resulta no sofrimento. Esse kamma conduz ao surgimento de kamma, não à cessação de kamma. </p>

<p class=Normal> (2) "Qualquer kamma criado através da raiva ... (3) Qualquer kamma criado criado através da delusão, nascido da delusão, causado pela delusão, originado da delusão, esse kamma é prejudicial e repreensível e resulta no sofrimento. Esse kamma conduz ao surgimento de kamma, não à cessação de kamma.</p>

<p class=Normal> "Essas são as três causas para a origem de kamma.</p>

<p class=Normal> “Bhikkhus, há essas outras três causas para a origem de kamma. Quais três? A não-cobiça é uma causa para a origem de kamma, a não-raiva é uma causa para a origem de kamma, e a não-delusão é uma causa para a origem de kamma.</p>

<p class=Normal> (1) "Qualquer kamma criado criado através da não-cobiça, nascido da não-cobiça, causado pela não-cobiça, originado da não-cobiça, esse kamma é benéfico e irrepreensível e resulta na felicidade. Esse kamma conduz à cessação de kamma, não ao surgimento de kamma. </p>

<p class=Normal> (2) "Qualquer kamma criado através da não-raiva ... (3) Qualquer kamma criado através da não-delusão, nascido da não-delusão, causado pela não-delusão, originado da não-delusão, esse kamma é benéfico e irrepreensível e resulta na felicidade. Esse kamma conduz à cessação de kamma, não ao surgimento de kamma.</p>

<p class=Normal> "Essas são as outras três causas para a origem de kamma.</p>

