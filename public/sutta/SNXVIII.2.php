<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim ouvi.
Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no
Parque de Anathapindika. Então o Venerável Rahula se aproximou do Abençoado e
depois de cumprimentá-lo, sentou a um lado e disse:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, seria bom se o Abençoado me ensinasse o Dhamma de forma resumida, de modo
que, tendo ouvido o Dhamma do Abençoado eu possa permanecer só, isolado,
diligente, ardente e decidido.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Rahula, o
que você pensa? As formas são permanentes ou impermanentes?” – “Impermanentes,
venerável senhor.” – “Aquilo que é impermanente é sofrimento ou felicidade?” –
“Sofrimento, venerável senhor.” – “Aquilo que é impermanente, sofrimento e
sujeito à mudança é adequado que se considere assim: “Isso é meu, isso eu sou,
isso é o meu eu?” – “Não, venerável senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Os sons
... aromas .... sabores ... tangíveis ... objetos mentais são permanentes ou
impermanentes?” – “Impermanentes, venerável senhor.” – “Aquilo que é
impermanente é sofrimento ou felicidade?” – “Sofrimento, venerável senhor.” –
“Aquilo que é impermanente, sofrimento e sujeito à mudança é adequado que se
considere assim: “Isso é meu, isso eu sou, isso é o meu eu?” – “Não, venerável
senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Vendo
dessa forma, Rahula, um nobre discípulo bem instruído experimenta o
desencantamento em relação às formas, desencantamento em relação aos sons,
desencantamento em relação aos aromas, desencantamento em relação aos sabores,
desencantamento em relação aos tangíveis, desencantamento em relação aos
objetos mentais. Experimentando o desencantamento ele se desapega. Através do
desapego a sua mente é libertada. Quando ela está libertada surge o
conhecimento: ‘Libertada.’ Ele compreende que: ‘O nascimento foi destruído, a
vida santa foi vivida, o que deveria ser feito foi feito, não há mais vir a ser
em nenhum estado.’” <b style='mso-bidi-font-weight:normal'><o:p></o:p></b></span></p>


