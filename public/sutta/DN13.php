<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião o Abençoado estava perambulando por Kosala com uma
grande sangha de bhikkhus até que por fim acabou chegando em um vilarejo
brâmane denominado Manasakata e se instalou ao norte do vilarejo em um
manguezal às margens do Rio Aciravati.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. Agora,
naquela ocasião muitos brâmanes prósperos e bem conhecidos estavam em
Manasakata, isto é, o brâmane Canki, o brâmane Tarukkha, o brâmane Pokkharasati, o brâmane Janussoni, o brâmane Todeyya.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. Vasettha
e Bharadvaja estavam caminhado ao longo da estrada, como costumavam fazer,
quando iniciaram uma discussão sobre o tema do caminho correto e do incorreto.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. O
estudante Brâmane Vasettha disse: “Este é o único caminho correto, este é o
caminho direto, o caminho da salvação que conduz aquele que o segue à união com
Brahma, da forma como foi ensinado pelo Brâmane Pokkharasati!”[</span><a
href="#N1"><span lang=PT-BR style='mso-ansi-language:PT-BR'>1</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>5. E o estudante Brâmane Bharadvaja disse: “Este é o único caminho correto
… da forma como foi ensinado pelo Brâmane Tarukkha!”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. Mas o
estudante brâmane Vasettha não foi capaz de convencer o estudante brâmane
Bharadvaja, nem o estudante brâmane Bharadvaja foi capaz de convencer o
estudante brâmane Vasettha.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. Então o
estudante brâmane Vasettha disse para o estudante brâmane Bharadvaja: “O
contemplativo Gotama está ao norte do vilarejo e acerca desse
contemplativo existe essa boa reputação: ‘Esse Abençoado é um arahant ,
perfeitamente iluminado, consumado no verdadeiro conhecimento e conduta,
bem-aventurado, conhecedor dos mundos, um líder insuperável de pessoas
preparadas para serem treinadas, mestre de devas e humanos, desperto, sublime.
Ele declara - tendo realizado por si próprio com o conhecimento direto - este
mundo com os seus devas, maras e brahmas, esta população com seus contemplativos
e brâmanes, seus príncipes e povo. Ele ensina o Dhamma, com o significado e
fraseado corretos, que é admirável no início, admirável no meio, admirável no
final; e ele revela uma vida santa que é completamente perfeita e imaculada. É
bom poder encontrar alguém tão nobre.’ Vamos até o contemplativo Gotama
perguntar-lhe sobre isso, aquilo que ele nos disser, deveríamos aceitar.” E o
estudante brâmane Bharadvaja concordou.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. Assim,
os dois foram até o Abençoado e eles se cumprimentaram. </span>Quando a
conversa cortês e amigável havia terminado eles<span lang=PT-BR
style='mso-ansi-language:PT-BR'> sentaram a um lado e Vasettha disse: “Mestre
Gotama, quando estávamos caminhando ao longo da estrada começamos a discutir
sobre os caminhos corretos e incorretos. Eu disse: ‘Este é o único caminho
correto … da forma como foi ensinado pelo Brâmane Pokkharasati’ e Bharadvaja
disse: ‘Este é o único caminho correto … da forma como foi ensinado pelo
Brâmane Tarukkha.’ Essa é a nossa disputa, a nossa rixa, nossa diferença.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. “Então,
Vasettha, você diz que o caminho para a união com Brahma é ensinado pelo
Brâmane Pokkharasati e Bharadvaja diz que ele é ensinado pelo Brâmane Tarukha.
Mas sobre o que é a disputa, a rixa, a diferença?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10.
“Caminhos corretos e incorretos Mestre Gotama. Existem vários tipos de Brâmanes que
ensinam diferentes caminhos: o Addhariya, o Tittiriya, o Chandoka, o Chandava,
o Brahmacariya - todos esses caminhos conduzem à união com Brahma?
Como se houvesse próximo a uma cidade ou vilarejo muitos caminhos diferentes,
todos esses caminhos chegariam juntos no mesmo lugar? E da mesma forma, os
caminhos dos vários Brâmanes … conduzem aqueles que os seguem à união com
Brahma?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. “Você
diz: ‘Eles conduzem,’ Vasettha?” - “Eu digo: ‘Eles conduzem,’ Mestre Gotama.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Você diz:
‘Eles conduzem,’ Vasettha?” - “Eu digo: ‘Eles conduzem,’ Mestre Gotama.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Você diz:
‘Eles conduzem,’ Vasettha?” - “Eu digo: ‘Eles conduzem,’ Mestre Gotama.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. “Mas,
Vasettha, existe pelo menos um desses brâmanes, mestres nos três Vedas, que
tenha visto Brahma cara a cara?” - “Não Mestre Gotama.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então o
mestre de algum desses mestres viu Brahma cara a cara?” - “Não, Mestre Gotama.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então os
ancestrais até a sétima geração passada do mestre de algum desses mestres viu
Brahma cara a cara?” - “Não, Mestre Gotama.”<o:p></o:p></span></p>



<p class=Normal><a name=T13></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>13. “Como então, Vasettha, os antigos brâmanes videntes, os criadores
dos mantras, os compositores dos mantras antigos, que antigamente eram
recitados, falados e compilados, e que ainda hoje os brâmanes recitam e repetem,
repetindo o que foi dito e recitando o que foi recitado – isto é, Atthaka,
Vamaka, Vamadeva, Vessamitta, Yamataggi, Angirasa, Bharadvaja, Vasettha,
Kassapa e Bhagu [<a href="#N2">2</a>] <a name=R2></a>– eles alguma vez
disseram: ‘Nós sabemos e vemos como e onde Brahma aparece’?” [</span><a
href="#N3"><span lang=PT-BR style='mso-ansi-language:PT-BR'>3</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R3></a> - “Não, Mestre
Gotama.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&nbsp;14.
“Portanto, Vasettha, nenhum desses Brâmanes, mestres nos três Vedas, viu Brahma
cara a cara, tampouco um dos mestres desses Brâmanes, ou os mestres dos
mestres, nem mesmo as últimas sete gerações ancestrais de um dos mestres. Nem
mesmo um dos antigos sábios poderia dizer: ‘Nós sabemos e vemos, quando, como e
onde Brahma aparece.’ Então aquilo que esses Brâmanes, mestres nos três Vedas
estão dizendo é: ‘Nós ensinamos este caminho para a união com Brahma que nós
não conhecemos nem vemos, este é o único caminho correto … conduzindo à união
com Brahma.’ O que você pensa, Vasettha? Sendo esse o caso, aquilo que esses
Brâmanes declaram não se mostra sem fundamento?” - “Sim, de fato, Mestre Gotama.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. “Bem,
Vasettha, quando esses Brâmanes, mestres nos três Vedas ensinam um caminho que
eles não conhecem nem vêm, dizendo: ‘Esse é o único caminho direto …,’ não é
possível que isso seja correto. Como uma fila de homens cegos segurando um no
outro, o primeiro nada vê, o do meio nada vê e o último nada vê – [</span><a
href="#N4"><span lang=PT-BR style='mso-ansi-language:PT-BR'>4</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R4></a>assim é a conversa
desses Brâmanes, mestres nos três Vedas: o primeiro nada vê, o do meio nada vê,
o último nada vê. A conversa desses Brâmanes, mestres nos três Vedas aparece
como motivo de risos, como meras palavras, vazias e inúteis.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16. “O que
você pensa, Vasettha? Esses Brâmanes, mestres nos três Vedas, vêm o sol e a lua
da mesma forma como as outras pessoas os vêm, e quando o sol e a lua nascem e
se põem, eles rezam, cantam louvores e veneram com as mãos postas?” - “Eles assim
fazem, Mestre Gotama.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17. “O que
você pensa, Vasettha? Esses Brâmanes, mestres nos três Vedas, que vêm o sol e a
lua da mesma forma que as outras pessoas, eles podem indicar um caminho para a
união com o sol e a lua, dizendo: ‘Este é o único caminho … que conduz à união
com o sol e a lua’?” - “Não, Mestre Gotama.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18.
“Portanto, Vasettha, esses Brâmanes, mestres nos três Vedas não podem indicar
um caminho para a união com o sol e a lua, vistos por eles. E, também, nenhum
deles viu Brahma cara a cara … nem mesmo as últimas sete gerações ancestrais de
um dos mestres. Nem mesmo um dos antigos sábios poderia dizer: ‘Nós sabemos e
vemos quando, como e onde Brahma aparece.’ Aquilo que esses Brâmanes declaram
não se mostra sem fundamento?” - “Sim, de fato, Mestre Gotama.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>19.
“Vasettha, suponha que um homem dissesse: ‘Eu estou apaixonado pela moça mais
bonita deste país.’ Então eles lhe perguntariam: ‘Bom homem, essa moça mais
bonita deste país pela qual você está apaixonado – você sabe se ela é da classe
nobre ou da classe dos brâmanes, ou da classe dos comerciantes, ou da classe
dos trabalhadores?’ e ele responderia: ‘Não.’ Então eles lhe perguntariam: ‘Bom
homem, essa moça mais bonita deste país pela qual você está apaixonado – você
sabe o nome e o clã dela? … Se ela é alta ou baixa ou com estatura média? … Se
ela tem a complexão escura, clara ou dourada? … Qual vilarejo, vila ou cidade
ela vive?’ e ele responderia: ‘Não.’ E então eles lhe perguntariam: ‘Bom homem,
você então está apaixonado por uma moça que você nem conhece ou viu?’ e ele
responderia: ‘Sim.’ O que você pensa, Vasettha, em sendo assim, a conversa daquele
homem não seriam apenas tolices?” - “Com certeza, Mestre Gotama.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>20. “Então,
Vasettha, é assim: nenhum desses Brâmanes … viu Brahma cara a cara, tampouco um
dos mestres desses Brâmanes …” - “Sim, de fato, Mestre Gotama.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Correto, Vasettha.
Quando esses Brâmanes, mestres nos três Vedas ensinam um caminho que eles não
conhecem e vêm, não é possível que isso seja correto.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>21.
“Vasettha, é como se um homem fosse construir uma escadaria para um palácio
numa encruzilhada. As pessoas poderiam dizer: ‘Essa escadaria para o palácio –
você sabe se a frente do palácio estará para o leste ou oeste, norte ou sul ou
se o palácio será alto, baixo ou médio?’ e ele diria: ‘Não.’ E elas poderiam
dizer: ‘Bem então, você não sabe ou imagina para que tipo de palácio está
construindo a escadaria?’ e ele responderia: ‘Não.’ A conversa daquele homem
não seriam apenas tolices?” - “Com certeza, Mestre Gotama.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>22-23.
(igual ao verso 20) <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>24.
“Vasettha, é como se este Rio Aciravati estivesse cheio de água até a borda de
forma que um corvo pudesse nele beber, e um homem viesse desejando cruzar até a
outra margem, até o outro lado e, estando em pé na margem, ele chamasse: ‘Venha
para cá outra margem, venha para cá!’ O que você pensa, Vasettha? A outra margem
do Rio Aciravati viria para esta margem por conta do chamado, pedido, súplica
ou sedução daquele homem?” - “Não, Mestre Gotama.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>25. “Bem
então, Vasettha, aqueles Brâmanes, mestres nos três Vedas que com persistência
negligenciam aquilo que um Brâmane deveria fazer e com persistência fazem
aquilo que um Brâmane não deveria fazer, declarando: ‘Nós fazemos súplicas a
Indra, Soma, Varuna, Isana, Pajapati, Brahma, Mahiddhi, Yama.’ Mas esses
Brâmanes que com persistência negligenciam aquilo que um Brâmane deveria fazer
… poderão, como conseqüência do seu chamado, pedido, súplica ou sedução, obter
após a morte, na dissolução do corpo, a união com Brahma – isso não é
possível.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>26.
“Vasettha, é como se este Rio Aciravati estivesse cheio de água até a borda de
forma que um corvo pudesse nele beber, e um homem viesse desejando cruzar até a
outra margem … mas ele estivesse atado e aprisionado nesta margem por uma forte
corrente, com as mãos atrás das costas. O que você pensa, Vasettha? Aquele
homem seria capaz de cruzar até a outra margem?” - “Não, Mestre Gotama.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>27. “Da
mesma forma, Vasettha, na disciplina dos nobres esses cinco elementos do prazer
sensual são chamados de correntes e grilhões. Quais cinco? Formas conscientizadas
através do olho que são desejáveis, agradáveis e fáceis de serem gostadas,
conectadas com o desejo sensual e que provocam a cobiça. Sons conscientizados
através do ouvido … Aromas conscientizados através do nariz … Sabores conscientizados
através da língua … Tangíveis conscientizados através do corpo que são desejáveis,
agradáveis e fáceis de serem gostados, conectados com o desejo sensual e que
provocam a cobiça. Esses cinco elementos do prazer sensual na disciplina dos
nobres são chamados de correntes e grilhões. E, Vasettha, aqueles Brâmanes,
mestres nos três Vedas, estão escravizados, apaixonados por esses cinco
elementos do prazer sensual, dos quais eles desfrutam com um senso de culpa,
sem se dar conta do perigo, sem saber como escapar.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>28. “Mas
que esses Brâmanes, mestres nos três Vedas, que com persistência negligenciam
aquilo que um Brâmane deveria fazer … que estão escravizados por esses cinco
elementos do prazer sensual … sem saber como escapar, possam alcançar depois da
morte, com a dissolução do corpo, a união com Brahma – isso não é possível.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>29. “É como
se este Rio Aciravati estivesse cheio de água até a borda de forma que um corvo
pudesse nele beber, e um homem viesse desejando cruzar até a outra margem … e
ele se deitasse na margem, cobrindo a cabeça com um xale. O que você pensa,
Vasettha? Aquele homem seria capaz de chegar até a outra margem?” - “Não, Mestre
Gotama.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>30. “Da
mesma forma, Vasettha, na disciplina dos nobres esses cinco obstáculos são
chamados de obstruções, empecilhos, envolvedores, coberturas. Quais cinco? O
obstáculo do desejo sensual, da má vontade, da preguiça e torpor, da
inquietação e ansiedade, da dúvida. Esses cinco são chamados obstáculos,
obstruções, empecilhos, envolvedores, coberturas. E esses Brâmanes, mestres nos
três Vedas estão aprisionados, confinados, obstruídos, enredados por esses
cinco obstáculos. Mas que esses Brâmanes, mestres nos três Vedas, que com
persistência negligenciam aquilo que um Brâmane deveria fazer … que estão
aprisionados … enredados por esses cinco obstáculos, possam alcançar depois da
morte, com a dissolução do corpo, a união com Brahma – isso não é possível.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>31. “O que
você pensa, Vasettha? O que você ouviu dito pelos Brâmanes que são veneráveis,
anciãos, os mestres dos mestres? Brahma é sobrecarregado por esposa e riqueza
ou ele não é sobrecarregado?” - “Não é sobrecarregado, Mestre Gotama.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele é
cheio de raiva ou sem raiva?” - “Sem raiva, Mestre Gotama.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“‘Ele é
cheio de má vontade ou sem má vontade?” - “Sem má vontade, Mestre Gotama.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele é
impuro ou puro?” - “Puro, Mestre Gotama.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele é
disciplinado [</span><a href="#N5"><span lang=PT-BR style='mso-ansi-language:
PT-BR'>5</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>] <a
name=R5></a>ou indisciplinado?” - “Disciplinado, Mestre Gotama.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>32. “E o
que você pensa, Vasettha? Os Brâmanes, mestres nos três Vedas são
sobrecarregados por esposas e riqueza ou não sobrecarregados?” - “Sobrecarregados, Mestre Gotama.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eles são
cheios de raiva ou sem raiva?” - “Cheios de raiva, Mestre Gotama.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eles são
cheios de má vontade ou sem má vontade?” - “Cheios de má vontade, Mestre Gotama.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eles são
impuros ou puros?” - “Impuros, Mestre Gotama.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eles são disciplinados
ou indisciplinados?” - “Indisciplinados, Mestre Gotama.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>33.
“Portanto, Vasettha, os Brâmanes, mestres nos três Vedas são sobrecarregados
por esposas e riqueza e Brahma não é sobrecarregado. Existe alguma comunhão,
algo em comum entre esses Brâmanes sobrecarregados e Brahma que não é
sobrecarregado?” - “Não, Mestre Gotama.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>34. “Isso é
correto, Vasettha. Que esses Brâmanes sobrecarregados, mestres nos três Vedas,
possam após a morte, com a dissolução do corpo, estar unidos com Brahma que não
é sobrecarregado – isso não é possível.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>35. “Da
mesma forma, esses Brâmanes, mestres nos três Vedas cheios de raiva … cheios de
má vontade … impuros … indisciplinados, têm alguma comunhão, algo em comum com
o disciplinado Brahma?” - “Não, Mestre Gotama.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>36. “Isso é
correto, Vasettha. Que esses Brâmanes possam após a morte estar unidos com
Brahma isso não é possivel. Mas esses Brâmanes, mestres nos três Vedas, tendo
sentado à margem do rio, afundam com desespero, pensando poder encontrar uma
passagem segura para a outra margem. Por conseguinte o seu conhecimento
tríplice é chamado de o deserto tríplice, a selva tríplice, a destruição
tríplice.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>37. Depois
dessas palavras Vasettha disse: “Mestre Gotama, eu ouvi dizer: ‘O Contemplativo
Gotama sabe o caminho para a união com Brahma.”’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O que você
pensa, Vasettha? Suponha que houvesse um homem que tivesse nascido e crescido
em Manasakata, e alguém que viesse para Manasakata e estivesse perdido,
perguntasse qual era o caminho. Aquele homem, nascido e crescido em Manasakata,
ficaria num estado de confusão e perplexidade?” - “Não, Mestre Gotama. E por que
não? Porque aquele homem conheceria todos os caminhos.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>38.
“Vasettha, poder-se-ia dizer que aquele homem ao ser perguntado o caminho
pudesse ficar confuso e perplexo, mas o Tathagata, ao ser perguntado sobre o
mundo de Brahma e o caminho para chegar lá, com certeza não ficaria confuso ou
perplexo. Pois, Vasettha, eu conheço Brahma e o mundo de Brahma e o caminho
para o mundo de Brahma e o caminho da prática pelo qual o mundo de Brahma
poderá ser conquistado.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>39. Com
isso Vasettha disse: “Mestre Gotama, eu ouvi dizer: ‘O Contemplativo Gotama
sabe o caminho para a união com Brahma.’ Seria bom se o Mestre Gotama pudesse
nos ensinar o caminho para a união com Brahma, que o Mestre Gotama ajude o povo
de Brahma!”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
Vasettha, ouça e preste muita atenção àquilo que eu vou dizer.” - “Sim, venerável
Senhor,” Vasettha respondeu. O Abençoado disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>40-75.
“Vasettha, um Tathagata surge no mundo, </span>um arahant , perfeitamente
iluminado, consumado no verdadeiro conhecimento e conduta, bem-aventurado,
conhecedor dos mundos, um líder insuperável de pessoas preparadas para serem
treinadas, mestre de devas e humanos, desperto, sublime<span lang=PT-BR
style='mso-ansi-language:PT-BR'>. Ele declara - tendo realizado por si próprio
com o conhecimento direto - este mundo com os seus devas, maras e brahmas, esta
população com seus contemplativos e brâmanes, seus príncipes e povo. Ele ensina o
Dhamma, com o significado e fraseado corretos, que é admirável no início, admirável
no meio, admirável no final; e ele revela uma vida santa que é completamente
perfeita e imaculada. Um discípulo segue a vida santa, pratica a virtude e
alcança o primeiro jhana. (igual ao </span><a href="DN2.php#T43"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>DN 2, versos 43-75</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>)<o:p></o:p></span></p>



<p class=Normal><a name=T76></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>76. “Então, com o coração pleno de amor bondade, ele permanece permeando
o primeiro quadrante com a mente imbuída de amor bondade, da mesma forma o
segundo, da mesma forma o terceiro, da mesma forma o quarto; assim, acima,
abaixo, em volta e em todos os lugares, para todos bem como para si mesmo, ele
permanece permeando o mundo todo com a mente imbuída de amor bondade,
abundante, transcendente, imensurável, sem hostilidade e sem má vontade.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>77. “Como
se um poderoso trompetista fizesse com pouca dificuldade uma proclamação aos
quatro quadrantes, da mesma forma através desta meditação, Vasettha, através
desta libertação da mente através do amor bondade ele não deixa nada sem ser
tocado, nada no reino do sensual sem ser tocado. [</span><a href="#N6"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>6</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>] <a name=R6></a>Esse, Vasettha, é o caminho
para a união com Brahma.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>78. “Então,
com o coração pleno de compaixão … alegria altruísta … equanimidade ele
permanece permeando o primeiro quadrante com a mente imbuída de equanimidade,
da mesma forma o segundo, da mesma forma o terceiro, da mesma forma o quarto;
assim, acima, abaixo, em volta e em todos os lugares, para todos bem como para
si mesmo, ele permanece permeando o mundo todo com a mente imbuída de
equanimidade, abundante, transcendente, imensurável, sem hostilidade e sem má
vontade.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>79. “Como
se um poderoso trompetista fizesse com pouca dificuldade uma proclamação aos
quatro quadrantes, da mesma forma através desta meditação, Vasettha, através
desta libertação da mente através da compaixão … alegria altruísta … equanimidade
ele não deixa nada sem ser tocado, nada no reino do sensual sem ser
tocado. Esse, Vasettha, é o caminho para a união com Brahma.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>80. “O que
você pensa, Vasettha? Um bhikkhu que assim permaneça será sobrecarregado por esposa
e riqueza ou não sobrecarregado?” - “Não sobrecarregado, venerável Gotama. Ele
não terá raiva … má vontade … será puro e disciplinado, venerável Gotama.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>81. “Então,
Vasettha, o bhikkhu não é sobrecarregado e Brahma não é sobrecarregado. Esse
bhikkhu não sobrecarregado tem algo em comum com Brahma não sobrecarregado?” -
“Sim, de fato, venerável Gotama.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Isso é
correto, Vasettha. Então que esse bhikkhu não sobrecarregado, após a morte,
com a dissolução do corpo, possa alcançar a união com Brahma não
sobrecarregado – isso é possivel. Da mesma forma um bhikkhu sem raiva … sem má
vontade … puro … disciplinado … Então, que esse bhikkhu disciplinado, após a
morte, com a dissolução do corpo, possa alcançar a união com Brahma disciplinado – isso é
possível.”<o:p></o:p></span></p>



<p class=Normal><a name=R7></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>82. Depois que isso foi dito os jovens brâmanes Vasettha e Bharadvaja
disseram para o Abençoado: “Magnífico, Mestre Gotama! Magnífico, Mestre Gotama!
Mestre Gotama esclareceu o Dhamma de várias formas, como se tivesse colocado em
pé o que estava de cabeça para baixo, revelasse o que estava escondido,
mostrasse o caminho para alguém que estivesse perdido ou segurasse uma lâmpada
no escuro para aqueles que possuem visão pudessem ver as formas. Nós buscamos
refúgio no Mestre Gotama, no Dhamma e na Sangha dos bhikkhus. Que o Mestre
Gotama nos aceite como discípulos leigos que nele buscaram refúgio para o resto
da vida.”[</span><a href="#N7">7</a>]</p>




