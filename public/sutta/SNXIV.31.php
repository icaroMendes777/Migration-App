<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em Savathi.
“Bhikkhus, antes da minha iluminação, quando eu ainda era um Bodisatva não
desperto, eu pensei: ‘Qual é gratificação, qual é o perigo, qual é a escapatória
no caso do elemento terra? Qual é a gratificação, qual é o perigo, qual é a
escapatória no caso do elemento água? Qual é a gratificação, qual é o perigo,
qual é a escapatória no caso do elemento fogo? Qual é gratificação, qual é o
perigo, qual é a escapatória no caso do elemento ar?’<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
bhikkhus, me ocorreu que: ‘O prazer e a alegria que surgem na dependência do
elemento terra: essa é a gratificação do elemento terra. Que o elemento terra
seja impermanente, insatisfatório e sujeito à mudança: esse é o perigo no
elemento terra. A remoção e o abandono do desejo e cobiça pelo elemento terra:
essa é a escapatória do elemento terra. <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O prazer e
a alegria que surgem na dependência do elemento água ... elemento fogo ...
elemento ar: essa é a gratificação do elemento ar. Que o elemento ar seja
impermanente, insatisfatório e sujeito à mudança: esse é o perigo no elemento
ar. A remoção e o abandono do desejo e cobiça pelo elemento ar: essa é a
escapatória do elemento ar. <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Enquanto,
bhikkhus, eu não soube através do conhecimento direto como na verdade é a
gratificação, o perigo e a escapatória no caso dos quatro elementos, eu não
reivindiquei ter despertado para a insuperável e perfeita iluminação neste
mundo com as suas divindades, Maras e Brahmas, nesta população com os seus
contemplativos e brâmanes, </span>seus príncipes e povo<span lang=PT-BR
style='mso-ansi-language:PT-BR'>. Mas quando através do conhecimento direto eu
soube tudo isso tal como na verdade é, então reivindiquei<span
style="mso-spacerun: yes">  </span>ter despertado para a insuperável e perfeita
iluminação neste mundo com ... </span>seus príncipes e povo<span lang=PT-BR
style='mso-ansi-language:PT-BR'>.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Surgiram
em mim a visão e o conhecimento: ‘Inabalável é a libertação da minha mente.
Este é o último nascimento. Não há mais vir a ser a nenhum estado.’”<b
style='mso-bidi-font-weight:normal'><o:p></o:p></b></span></p>


