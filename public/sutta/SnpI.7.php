<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim ouvi.
Em certa ocasião o Abençoado estava<span style='mso-spacerun:yes'>  </span>em
Savatthi no Bosque de Jeta, no Parque de Anathapindika. Então, ao amanhecer, o
Abençoado se vestiu e tomando a tigela e o manto externo, foi para Savatthi
para esmolar alimentos. Agora naquela ocasião um fogo estava ardendo e uma
oferenda estava sendo preparada na casa do brâmane Aggikabharadvaja. Então o
Abençoado enquanto esmolava alimentos chegou na casa do brâmane. O brâmane
vendo o Abençoado vindo à distância, disse o seguinte: “Fique aí, você careca,
fique aí monge desprezível, fique aí pária.” Depois que ele assim falou o
Abençoado disse para o brâmane: “Você sabe, brâmane, quem é um pária e quais
são as condições que fazem um pária?” “Não, de fato, Venerável Gotama, eu não
sei quem é um pária e quais são as condições que fazem um pária. Seria bom se o
Venerável Gotama me explicasse o Dhamma de modo que eu possa saber quem é um
pária e quais são as condições que fazem um pária.” [<a href="#N1">1</a>] <o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Então ouça e preste muita atenção àquilo que eu vou dizer.” – “Sim,
venerável senhor,” o brâmane respondeu. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. “Todo
aquele que tem raiva, abriga a raiva e reluta falar bem dos outros (desacredita
a bondade dos outros), tem as idéias pervertidas, enganador – saiba que ele é
um pária.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. “Todo
aquele que neste mundo mata seres vivos, que nasceram uma vez ou nasceram duas
vezes,[<a href="#N2">2</a>] <a name=R2></a>que não tem compaixão pelos seres
vivos – saiba que ele é um pária. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. “Todo
aquele que destrói e sitia vilarejos e aldeias e ganha notoriedade como
opressor – saiba que ele é um pária. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. “Quer
seja num vilarejo ou na floresta, todo aquele que rouba aquilo que pertence a
outrem, que toma aquilo que não lhe foi dado – saiba que ele é um pária. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. “Todo
aquele que tendo tomado um empréstimo foge quando é cobrado, dizendo, ‘Eu <i
style='mso-bidi-font-style:normal'>não</i> lhe devo nada’ – saiba que ele é um
pária. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. “Todo
aquele que cobiçando algo, mata uma pessoa e agarra aquilo que a pessoa tenha –
saiba que ele é um pária. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. “Todo
aquele que em seu próprio benefício ou em benefício de outros ou para obter
riqueza, mente quando é questionado como testemunha – saiba que ele é um pária.
<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. “Todo
aquele que por meio da força ou com consentimento, se envolve em encontros
amorosos com esposas de parentes ou amigos – saiba que ele é um pária. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. “Todo
aquele que sendo próspero não sustenta a sua mãe ou pai que estão velhos –
saiba que ele é um pária. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. “Todo
aquele que ataca e ofende com a linguagem grosseira a mãe, pai, irmão, irmã,
sogra ou sogro – saiba que ele é um pária. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. “Todo
aquele que quando solicitado um bom conselho, fala o que é prejudicial e
emprega linguagem evasiva – saiba que ele é um pária. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. “Todo
aquele que tendo cometido uma ação ruim, deseja que outros não tomem
conhecimento daquilo e é um hipócrita – saiba que ele é um pária. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. “Todo
aquele que tendo sido convidado para a casa de outrem e compartindo de boa
comida, não retribui – saiba que ele é um pária. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14. “Todo
aquele que engana, dizendo mentiras, um brâmane ou contemplativo, ou qualquer
outro mendicante – saiba que ele é um pária. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. “Todo
aquele que quando um brâmane ou contemplativo aparece na hora do almoço o
provoca com linguagem grosseira e não lhe oferece esmolas<span
style='mso-spacerun:yes'>  </span>– saiba que ele é um pária. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16. “Todo
aquele que neste mundo, envolto na ignorância, faz previsões enganosas visando
obter algum ganho – saiba que ele é um pária. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17. “Todo
aquele que corrompido pelo seu orgulho, exalta a si mesmo e humilha os outros –
saiba que ele é um pária. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18. “Todo
aquele que se entrega à raiva, é mesquinho e tem desejos vis, é egoísta e
enganador, desavergonhado e destemido (em cometer o mal) – saiba que ele é um
pária. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>19. “Todo
aquele que insulta o Buda ou os seus discípulos, quer sejam contemplativos ou
chefes de família – saiba que ele é um pária. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>20. “Todo
aquele que não sendo um arahant, pretende sê-lo, é o maior patife em todo o
universo – ele é o mais baixo dos párias.<span style='mso-spacerun:yes'> 
</span><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>21. “Não é
devido ao nascimento que alguém é um pária; não é devido ao nascimento que
alguém é um brâmane. Através das ações alguém se torna um pária, através das
ações alguém se torna um brâmane.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>22. “Agora
ouça este exemplo (o fato que através do nascimento alguém não é um pária).
Havia o filho de um pária, Sopaka, que era conhecido como Matanga. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>23. “Esse
Matanga alcançou a fama mais elevada tão difícil de ser obtida. Muitos eram os
guerreiros (<i>khatiyas</i>) e brâmanes que vinham servi-lo. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>24.
“Destruindo as paixões mundanas ele entrou no nobre caminho e alcançou o reino
de Brahma. O seu nascimento como um pária não o impediu de renascer no reino de
Brahma.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>25.
“Aqueles brâmanes que conhecem os Vedas e que nasceram em uma família que
recita os Vedas, se eles se habituarem às ações ruins, <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>26. “Eles
não somente sofrem com a desgraça nesta vida mas na seguinte eles nascem num
estado pleno de sofrimento. A casta não impede a desgraça ou o nascimento miserável.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>27. “Não é
devido ao nascimento que alguém é um pária; não é devido ao nascimento que
alguém é um brâmane. Através das ações alguém se torna um pária, através das
ações alguém se torna um brâmane. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando o
Buda disse isso, o Brâmane Aggikabharadvaja disse para o Abençoado: “Magnífico,
Mestre Gotama! Magnífico, Mestre Gotama! Mestre Gotama esclareceu o Dhamma de
várias formas, como se tivesse colocado em pé o que estava de cabeça para
baixo, revelasse o que estava escondido, mostrasse o caminho para alguém que
estivesse perdido ou segurasse uma lâmpada no escuro para aqueles que possuem
visão pudessem ver as formas. Nós buscamos refúgio no Mestre Gotama, no Dhamma
e na Sangha dos bhikkhus. Que o Mestre Gotama me aceite como o
discípulo leigo que buscou refúgio para o resto da vida.”<o:p></o:p></span></p>

