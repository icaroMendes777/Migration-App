muitas vidas passadas nos seus modos e detalhes. Por que isso? Porque assim é
com aquele que realiza o treinamento contido na Revelação do Mestre.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>19.
&quot;Com a sua mente dessa forma concentrada, purificada...atingindo a
