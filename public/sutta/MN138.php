<p class=Normal><a name=N4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[4] MA: A mente está ‘estabelecida internamente’ por meio do apego a um
objeto interno. O próprio texto do sutta faz a mudança de <i>viññana </i>mencionado
no sumário do Buda para <i>citta </i>na explicação de Maha Kaccana.<br> De acordo com Ajaan Bramali a versão do Majjhima Agama deste sutta menciona "<u>não</u> estabelecida internamente" que é o contrário da versão em Pali. A versão dos Agamas faz mais sentido no contexto deste sutta e parece que a versão em Pali contém um erro de transmissão. Em resumo, se a mente "seguir atrás, estiver atada, presa, agrilhoada e vinculada" aos prazeres dos estados de jhana então a mente não será capaz de alcançar os estados de jhana ou se "estabelecer internamente". [<a
href="#R4">Retorna</a>]<o:p></o:p></span></p>

<p class=Normal><a name=N4A></a><span lang=PT-BR style='mso-ansi-language:
