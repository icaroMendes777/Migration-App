<p class=Normal><a name=N5></a> [5] </span><span lang=PT-BR
style='mso-ansi-language:PT-BR'>Todas as edições conhecidas do MN 138 em Pali
indicam neste trecho <i>anupada paritassana</i>, literalmente “agitação devido
ao não apego,” o que obviamente contradiz tudo aquilo que o Buda ensinou de
forma consistente: de que a agitação surge do apego e cessa com a remoção do
apego. Um sutta no Samyutta Nikaya (<a href="SNXXII.7.php">SN XXII.7</a>), é
virtualmente idêntico a este trecho do MN 138, exceto que ali a leitura é, como
seria esperado, <i>upada paritassana</i>, “agitação devido ao apego.” Do texto
do Samyutta podemos com segurança inferir que a leitura do Majjhima é um antigo
erro que deve ser desconsiderado. O trecho mencionado aqui está baseado no
texto do sutta do Samyutta Nikaya. [<a href="#R5">Retorna</a>]</span><span
lang=PT-BR style='mso-ansi-language:PT-BR'><o:p></o:p></span></p>


