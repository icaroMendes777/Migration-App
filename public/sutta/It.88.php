<p class=Normal> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: "Bhikkhus, há essas contaminações interiores, inimigos interiores, adversários interiores, assassinos interiores, oponentes interiores. Quais três? A cobiça, bhikkhus, é uma contaminação interior ... um oponente interior. A raiva é uma contaminação interior ... um oponente interior. A delusão é uma contaminação interior ... um oponente interior. Essas são as três."</p>

<p class=Normal style='margin-left:.75in'> A cobiça causa o infortúnio, <br>
a cobiça agita a mente; <br>
as pessoas não compreendem que<br>
esse é um perigo produzido no interior.</p>

<p class=Normal style='margin-left:.75in'> Uma pessoa cobiçosa não conhece o bem, <br>
uma pessoa cobiçosa não vê o Dhamma; <br>
a cegueira causada pela escuridão então prevalece<br>
quando a cobiça subjuga a pessoa.</p>

<p class=Normal style='margin-left:.75in'> Mas quem abandonou a cobiça<br>
não anseia por aquilo que estimula a cupidez, <br>
A cobiça dele desliza<br>
igual a água numa folha de lótus.</p>

<p class=Normal style='margin-left:.75in'> A raiva causa o infortúnio, <br>
a raiva agita a mente; <br>
as pessoas não compreendem que<br>
esse é um perigo produzido no interior.</p>

<p class=Normal style='margin-left:.75in'> Uma pessoa raivosa não conhece o bem, <br>
uma pessoa raivosa não vê o Dhamma; <br>
a cegueira causada pela escuridão então prevalece<br>
quando a raiva subjuga a pessoa.</p>

<p class=Normal style='margin-left:.75in'> Mas quem abandonou a raiva<br>
não anseia por aquilo que estimula a raiva, <br>
A raiva dele desliza<br>
igual a fruta do caule da palmyra.</p>

<p class=Normal style='margin-left:.75in'> A delusão causa o infortúnio, <br>
a delusão agita a mente; <br>
as pessoas não compreendem que<br>
esse é um perigo produzido no interior.</p>

<p class=Normal style='margin-left:.75in'> Uma pessoa deludida não conhece o bem, <br>
uma pessoa deludida não vê o Dhamma; <br>
a cegueira causada pela escuridão então prevalece<br>
quando a delusão subjuga a pessoa.</p>

<p class=Normal style='margin-left:.75in'> Mas quem abandonou a delusão<br>
não é confundida pelas coisas confusas. <br>
Ela dá um fim à delusão<br>
igual ao nascer do sol que dá um fim à escuridão.</p>


