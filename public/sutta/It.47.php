<p class=Normal> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: “Bhikkhus, um bhikkhu deve ser vigilante: ele deve permanecer com atenção plena, plena consciência, concentrado, satisfeito e purificado, e deve nos momentos apropriados cultivar o insight daquelas qualidades mentais benéficas. Bhikkhus, para um bhikkhu vigilante e que assim permanece, um de dois frutos podem ser esperados: completa compreensão aqui e agora, ou se houver alguma contaminação residual, o estado de não-retorno."</p>


<p class=Normal style='margin-left:.75in'> Vigilantes ouçam isto: <br> 
despertem, vocês que estão dormidos! <br>
A vigilância é melhor do que o sono: <br>
não há medo no vigilante.</p>

<p class=Normal style='margin-left:.75in'> Quem está vigilante e plenamente atento, <br>
com plena consciência e concentrado, <br>
satisfeito e purificado, investigando corretamente o Dhamma<br>
com a mente unificada, com o tempo<br>
destruirá as trevas da ignorância.</p>

<p class=Normal style='margin-left:.75in'> Portanto dedique-se à vigilância, <br>
um bhikkhu ardente, inteligente, com os jhanas, <br>
tendo rompido o grilhão do nascimento e morte, <br>
aqui e agora poderá realizar <br>
o supremo despertar.</p>




