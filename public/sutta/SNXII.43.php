<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, eu ensinarei para vocês a origem e a cessação do
sofrimento. </span>Ouçam e prestem muita atenção àquilo que eu vou dizer.” –
“Sim, venerável senhor,” os bhikkhus responderam. O Abençoado disse o seguinte<span
lang=PT-BR style='mso-ansi-language:PT-BR'>:<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>‘E o que,
bhikkhus, é a origem do sofrimento? Na dependência do olho e das formas, a
consciência no olho surge. O encontro dos três é o contato. Com o contato como
condição, a sensação [surge]; com a sensação como condição, o desejo. Essa é a
origem do sofrimento. <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Na
dependência do ouvido e dos sons ... na dependência do nariz e dos aromas ...
na dependência da língua e dos sabores ... na dependência do corpo e dos
tangíveis .... na dependência da mente e dos objetos mentais, a consciência na
mente surge. O encontro dos três é o contato. Com o contato como condição, a
sensação [surge]; com a sensação como condição, o desejo. Essa é a origem do
sofrimento.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>‘E o que,
bhikkhus, é a cessação do sofrimento? Na dependência do olho e das formas, a
consciência no olho surge. O encontro dos três é o contato. Com o contato como
condição, a sensação [surge]; com a sensação como condição, o desejo. Mas com o
desaparecimento e cessação sem deixar vestígios desse mesmo desejo, cessa o
apego; com a cessação do apego, cessa o ser/existir; com a cessação do
ser/existir, cessa o nascimento; com a cessação do nascimento, envelhecimento e
morte, lamentação, dor, angústia e desespero, tudo cessa. Essa é a cessação de
toda essa massa de sofrimento. Essa é a cessação do sofrimento. <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Na
dependência do ouvido e dos sons ... na dependência do nariz e dos aromas ...
na dependência da língua e dos sabores ... na dependência do corpo e dos
tangíveis .... na dependência da mente e dos objetos mentais, a consciência na
mente surge. O encontro dos três é o contato. Com o contato como condição, a
sensação [surge]; com a sensação como condição, o desejo. Mas com o
desaparecimento e cessação sem deixar vestígios desse mesmo desejo, cessa do
apego; com a cessação do apego, a cessação do ser/existir; com a cessação do
ser/existir, a cessação do nascimento; com a cessação do nascimento,
envelhecimento e morte, lamentação, dor, angústia e desespero, tudo cessa. Essa
é a cessação de toda essa massa de sofrimento. Essa é a cessação do
sofrimento.”<b style='mso-bidi-font-weight:normal'><o:p></o:p></b></span></p>


