<p class=Normal>
Ouvi que em certa ocasião o Abençoado estava em Rajagaha, no mangueiral
de Jivaka. Então Jivaka Komarabhacca foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse: <o:p></o:p></span></p>


<p class=Normal>
&quot;Venerável senhor, de que modo alguém é um discípulo leigo?&quot; <o:p></o:p></span></p>

<p class=Normal>
&quot;Jivaka, quando alguém busca refúgio no Buda, busca refúgio no
Dhamma, busca refúgio na Sangha, então desse modo alguém é um discípulo
leigo.&quot; <o:p></o:p></span></p>


<p class=Normal>
&quot;De que modo, venerável senhor, <span style="mso-spacerun:
yes"> </span>alguém é um discípulo leigo virtuoso?&quot;<span
style="mso-spacerun: yes">  </span><o:p></o:p></span></p>


<p class=Normal>"Quando, Jivaka, um discípulo leigo se abstém de tirar a vida de outros seres, se abstém de tomar aquilo que não for dado, se abstém da conduta sexual imprópria, se abstém da linguagem mentirosa, se abstém do vinho, álcool e outros embriagantes que causam a negligência, desse modo alguém é um discípulo leigo virtuoso." </p>


<p class=Normal>"De que modo, venerável senhor, um discípulo leigo pratica para o seu próprio benefício mas não para o benefício dos outros?" </p>

<p class=Normal> (1) 'Quando, Jivaka, um discípulo leigo é perfeito na fé, mas não encoraja os outros que aperfeiçoem a sua fé; (2) quando ele observa o comportamento virtuoso, mas não encoraja os outros que observem o comportamento virtuoso; (3) quando ele é perfeito na generosidade, mas não encoraja os outros que aperfeiçoem a sua generosidade; (4) quando ele deseja ver os bhikkhus, mas não encoraja os outros a verem os bhikkhus; (5) quando ele deseja ouvir o bom Dhamma, mas não encoraja os outros a ouvirem o bom Dhamma; (6) quando ele retém na mente os ensinamentos que ouviu, mas não encoraja os outros a reterem na mente os ensinamentos que ouviram; (7) quando ele examina o significado dos ensinamentos que reteve na mente, mas não encoraja os outros a examinarem o significado dos ensinamentos que retiveram na mente; (8) quando ele entende o significado e pratica de acordo com o Dhamma, mas não encoraja os outros a entenderem o significado e praticarem de acordo com o Dhamma: é desse modo, Jivaka, que um discípulo leigo pratica para o seu próprio benefício mas não para o benefício dos outros. </p>

<p class=Normal>"De que modo, venerável senhor, um discípulo leigo pratica para o seu próprio benefício e também para o benefício dos outros?" </p>

<p class=Normal> (1) 'Quando, Jivaka, um discípulo leigo é perfeito na fé, e também encoraja os outros que aperfeiçoem a sua fé; (2) quando ele observa o comportamento virtuoso, e também  encoraja os outros que observem o comportamento virtuoso; (3) quando ele é perfeito na generosidade, e também  encoraja os outros que aperfeiçoem a sua generosidade; (4) quando ele deseja ver os bhikkhus, e também  encoraja os outros a verem os bhikkhus; (5) quando ele deseja ouvir o bom Dhamma, e também  encoraja os outros a ouvirem o bom Dhamma; (6) quando ele retém na mente os ensinamentos que ouviu, e também  encoraja os outros a reterem na mente os ensinamentos que ouviram; (7) quando ele examina o significado dos ensinamentos que reteve na mente, e também  encoraja os outros a examinarem o significado dos ensinamentos que retiveram na mente; (8) quando ele entende o significado e pratica de acordo com o Dhamma, e também  encoraja os outros a entenderem o significado e praticarem de acordo com o Dhamma: é desse modo, Jivaka, que um discípulo leigo pratica para o seu próprio benefício e também  para o benefício dos outros."
