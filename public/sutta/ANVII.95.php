<p class=Normal> "Bhikkhus, há esses sete tipos de pessoas que são merecedoras de dádivas, merecedoras de hospitalidade, merecedoras de oferendas, merecedoras de saudações com reverência, um campo inigualável de mérito para o mundo. Quais sete? </p>

<p class=Normal> (1) "Aqui, bhikkhus, uma certa pessoa permanece contemplando a impermanência no olho, percebendo a impermanência, sentindo (experimentando) a impermanência constantemente, continuamente focando nisso com a mente sem interrupção, penetrando com a sabedoria. Realizando por si mesmo através do conhecimento direto, aqui e agora, ele entra e permanece na libertação da mente e libertação através da sabedoria que são imaculadas com a destruição de todas as impurezas. Esse é o primeiro tipo de pessoa merecedora de dádivas, merecedora de hospitalidade, merecedora de oferendas, merecedora de saudações com reverência, um campo inigualável de mérito para o mundo. </p>

<p class=Normal> (2) "Outra vez, uma certa pessoa permanece contemplando a impermanência no olho, percebendo a impermanência, sentindo (experimentando) a impermanência constantemente, continuamente focando nisso com a mente sem interrupção, penetrando com a sabedoria. Para ela a destruição das impurezas e o fim da vida ocorrem simultaneamente. Esse é o segundo tipo de pessoa merecedora de dádivas, merecedora de hospitalidade, merecedora de oferendas, merecedora de saudações com reverência, um campo inigualável de mérito para o mundo. </p>

<p class=Normal> (3) "Outra vez, uma certa pessoa permanece contemplando a impermanência no olho, percebendo a impermanência, sentindo (experimentando) a impermanência constantemente, continuamente focando nisso com a mente sem interrupção, penetrando com a sabedoria. Com a completa destruição dos cinco primeiros grilhões ela realiza nibbana no intervalo. Esse é o terceiro tipo de pessoa merecedora de dádivas, merecedora de hospitalidade, merecedora de oferendas, merecedora de saudações com reverência, um campo inigualável de mérito para o mundo. </p>

<p class=Normal> (4) " ... Com a completa destruição dos cinco primeiros grilhões ela realiza nibbana ao pousar. Esse é o quarto tipo de pessoa ... </p>

<p class=Normal> (5) " ... Com a completa destruição dos cinco primeiros grilhões ela realiza nibbana sem esforço. Esse é o quinto tipo de pessoa ... </p>

<p class=Normal> (6) " ... Com a completa destruição dos cinco primeiros grilhões ela realiza nibbana com esforço. Esse é o sexto tipo de pessoa ... </p>

<p class=Normal> (7) "Outra vez, uma certa pessoa permanece contemplando a impermanência no olho, percebendo a impermanência, sentindo (experimentando) a impermanência constantemente, continuamente focando nisso com a mente sem interrupção, penetrando com a sabedoria. Com a completa destruição dos cinco primeiros grilhões ela estará destinada a um plano superior, o plano Akanittha. Esse é o sétimo tipo de pessoa merecedora de dádivas, merecedora de hospitalidade, merecedora de oferendas, merecedora de saudações com reverência, um campo inigualável de mérito para o mundo. </p>

<p class=Normal>"Esses, bhikkhus, são os sete tipos de pessoas merecedoras de dádivas, merecedoras de hospitalidade, merecedoras de oferendas, merecedoras de saudações com reverência, um campo inigualável de mérito para o mundo." </p>

<p class=Normal> (96) - (102) "Bhikkhus, há esses sete tipos de pessoas que são merecedoras de dádivas, merecedoras de hospitalidade, merecedoras de oferendas, merecedoras de saudações com reverência, um campo inigualável de mérito para o mundo. Quais sete? </p>

<p class=Normal>"Aqui, bhikkhus, uma certa pessoa (96) permanece contemplando o sofrimento no olho ... (97) ... permanece contemplando o não-eu no olho ... (98) ... permanece contemplando a destruição no olho ... (99) ... permanece contemplando o desaparecimento no olho ... (100) ... permanece contemplando o desapego no olho ... (101) ... permanece contemplando a cessação no olho ... (102) ... permanece contemplando o abandono no olho ... </p>

<p class=Normal> (103) - (190) " ... Aqui uma certa pessoa permanece contemplando a impermanência no ouvido ... no nariz ... na língua ... no corpo ... na mente ... nas formas ... nos sons ... nos odores ... nos sabores ... nos objetos tangíveis ... o nos objetos mentais ... </p>

<p class=Normal> (191) - (238) " ... na consciência no olho ... na consciência no ouvido ... na consciência no nariz ... na consciência na língua ... na consciência no corpo ... na consciência na mente ... </p>

<p class=Normal> (239) - (286) " ... no contato no olho ... no contato no ouvido ... no contato no nariz ... no contato na língua ... no contato no corpo ... no contato na mente ... </p>

