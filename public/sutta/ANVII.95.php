<p class=Normal> (335) - (382) " ... na percepção das formas ... na percepção dos sons ... na percepção dos odores ... na percepção dos sabores ... na percepção dos objetos tangíveis ... na percepção dos objetos mentais ... </p>

<p class=Normal> (383) - (430) " ... na volição em relação às formas ... na volição em relação aos sons ... na volição em relação aos odores ... na volição em relação aos sabores ... na volição em relação aos objetos tangíveis ... na volição em relação aos objetos mentais ... </p>

