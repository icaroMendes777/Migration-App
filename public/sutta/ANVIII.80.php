<p class=Normal> 
&quot;<span class=SpellE>Bhikkhus</span>, existem
essas oito razões para a preguiça. Quais oito? <o:p></o:p></span></p>


<p class=Normal> 
&quot;Existe o caso em que um <span class=SpellE>bhikkhu</span>
tem algum trabalho a ser feito. O pensamento lhe ocorre: 'Eu terei que fazer
este trabalho. Porém quando tiver feito este trabalho, meu corpo estará
cansado. Por que não me deito?' Então ele se deita. Ele não faz um esforço para
alcançar o que ainda não foi alcançado, a realização do que ainda não foi
realizado. Essa é a primeira razão para preguiça<o:p></o:p></span></p>


<p class=Normal> 
&quot;Existe o caso em que um <span class=SpellE>bhikkhu</span>
fez algum trabalho. 
 O pensamento lhe ocorre: 'Eu fiz algum trabalho.
Agora que fiz o trabalho, meu corpo está cansado. Por que não me deito?' Então
ele se deita. Ele não faz um esforço para alcançar o que ainda não foi
alcançado, a realização do que ainda não foi realizado. Essa é a segunda razão
para preguiça<o:p></o:p></span></p>


<p class=Normal> &quot;Existe o caso em que um <span class=SpellE>bhikkhu</span> tem que
fazer uma viagem. O pensamento lhe ocorre: 'Eu terei que fazer esta viagem.
Porém quando tiver feito <span class=GramE>a</span> viagem, meu corpo estará
cansado. Por que não me deito?' Assim ele se deita. Ele não faz um esforço para
alcançar o que ainda não foi alcançado, atingir o que não foi atingido, realizar
o que ainda não foi realizado. Essa é a terceira razão para preguiça<o:p></o:p></span></p>


<p class=Normal> 
&quot;Existe o caso em que um <span class=SpellE>bhikkhu</span>
foi viajar. O pensamento lhe ocorre: 'Eu fui viajar. Agora que fui viajar, meu
corpo está cansado. Por que não me deito?' Assim ele se deita. Ele não faz um
esforço para alcançar o que ainda não foi alcançado, realizar o que ainda não
foi realizado. Essa é a quarta razão para preguiça.<o:p></o:p></span></p>


<p class=Normal> 
&quot;Então existe o caso em que um <span
class=SpellE>bhikkhu</span>, que tendo ido esmolar alimentos num vilarejo ou
cidade, não ganha tanta comida comum ou refinada quanto necessita para se
satisfazer. O pensamento lhe ocorre: 'Ao esmolar alimentos num vilarejo ou
cidade, não ganhei tanta comida comum ou refinada quanto necessito para me
satisfazer. Este meu corpo está cansado e inadequado para o trabalho. Por que
não me deito?' Assim ele se deita. Ele não faz um esforço para alcançar o que
ainda não foi alcançado, atingir o que não foi atingido, realizar o que ainda
não foi realizado. Essa é a quinta razão para preguiça<o:p></o:p></span></p>


<p class=Normal> 
&quot;Então existe o caso em que um <span
class=SpellE>bhikkhu</span>, que tendo ido esmolar alimentos num vilarejo ou
cidade, ganha tanta comida comum ou refinada quanto necessita para se
satisfazer. O pensamento lhe ocorre: 'Ao esmolar alimentos num vilarejo ou
cidade,<span style='mso-spacerun:yes'>  </span>ganhei tanta
comida comum ou refinada quanto necessito para me satisfazer. Este meu corpo
está pesado e inadequado para o trabalho, como se eu estivesse com muitos meses
de gravidez. Por que não me deito?' Assim ele se deita. Ele não faz um esforço
para alcançar o que ainda não foi alcançado, atingir o que não foi
atingido,<span style='mso-spacerun:yes'>  </span>realizar o que ainda não foi
realizado. Essa é a sexta razão para preguiça<o:p></o:p></span></p>


<p class=Normal> 
&quot;Então existe o caso em que um <span
class=SpellE>bhikkhu</span> está abatido por uma leve enfermidade. O pensamento lhe
ocorre: 'Eu estou abatido por<span style='mso-spacerun:yes'>  </span>uma leve
enfermidade. É necessário deitar.' Assim ele se deita. Ele não faz um esforço
para alcançar o que ainda não foi alcançado, atingir o que não foi atingido,<span
style='mso-spacerun:yes'>  </span>realizar o que ainda não foi realizado. Essa
é a sétima razão para preguiça<o:p></o:p></span></p>


<p class=Normal> &quot;Então existe o caso em que um <span class=SpellE>bhikkhu</span>
se recuperou de sua enfermidade, não muito depois de sua recuperação o
pensamento lhe ocorre: 'Eu me recuperei da minha enfermidade<span class=GramE>,.</span>
Não<span style='mso-spacerun:yes'>  </span>muito tempo depois da minha
recuperação o meu corpo está fraco e inadequado para o trabalho. Por que não me
deito?' Então ele se deita. Ele não faz um esforço para alcançar o que ainda não
foi alcançado, atingir o que não foi atingido,<span style='mso-spacerun:yes'> 
</span>realizar o que ainda não foi realizado. Essa é a oitava razão para
preguiça<o:p></o:p></span></p>


<p class=Normal> 
&quot;Essas são as oito razões para preguiça. <o:p></o:p></span></p>


<p class=Normal> 
&quot;Existem essas seis oito razões para o estímulo
da energia. Quais oito? <o:p></o:p></span></p>


<p class=Normal> 
&quot;Existe o caso em que um <span class=SpellE>bhikkhu</span>
tem algum trabalho a ser feito. O pensamento lhe ocorre: 'Eu terei que fazer
este trabalho. Porém enquanto estiver fazendo esse trabalho, não será fácil
ocupar-me com a mensagem do Buda. Porque não faço um esforço antecipado para
alcançar o que ainda não foi alcançado, atingir o que não foi atingido,
realizar o que ainda não foi realizado.' Assim ele faz um esforço para alcançar
o que ainda não foi alcançado, atingir o que não foi atingido, realizar o que
ainda não foi realizado. Essa é a primeira razão para o estimulo da energia<o:p></o:p></span></p>


<p class=Normal> 
&quot;Existe o caso em que um <span class=SpellE>bhikkhu</span>
fez algum trabalho. O pensamento lhe ocorre: 'Eu fiz o trabalho.
Enquanto fazia o trabalho, não pude ocupar-me com a mensagem do Buda. Porque
não faço um esforço para alcançar o que ainda não foi alcançado, atingir o que
não foi atingido, realizar o que ainda não foi realizado.' Assim ele faz um
esforço para alcançar o que ainda não foi alcançado,<span
style='mso-spacerun:yes'>  </span>atingir o que não foi atingido, realizar o
que ainda não foi realizado. Essa é a segunda razão para o estimulo da energia<o:p></o:p></span></p>


<p class=Normal> 
&quot;Existe o caso em que um <span class=SpellE>bhikkhu</span>
tem que fazer uma viagem O pensamento lhe ocorre: 'Eu terei que fazer esta
viagem. Porém enquanto estiver fazendo essa viagem, não será fácil ocupar-me
com a mensagem do Buda. Porque não faço um esforço antecipado para alcançar o
que ainda não foi alcançado, atingir o que não foi atingido, realizar o que
ainda não foi realizado.' Assim ele faz um esforço para alcançar o que ainda não
foi alcançado,<span style='mso-spacerun:yes'>  </span>atingir o que não foi
atingido, realizar o que ainda não foi realizado. Essa é a terceira razão para
o estimulo da energia<o:p></o:p></span></p>


<p class=Normal> 
&quot;Existe o caso em que um <span class=SpellE>bhikkhu</span>
foi viajar o pensamento lhe ocorre: 'Eu fui viajar. Enquanto
viajava, não pude ocupar-me com a mensagem do Buda. Porque não faço um esforço
para alcançar o que ainda não foi alcançado,<span style='mso-spacerun:yes'> 
</span>atingir o que não foi atingido, realizar o que ainda não foi realizado.'
Assim ele faz um esforço para alcançar o que ainda não foi alcançado, atingir o
que não foi atingido, realizar o que ainda não foi realizado. Essa é a quarta
razão para o estimulo da energia<o:p></o:p></span></p>

<p class=Normal> 
&quot;Então existe o caso em que um <span
class=SpellE>bhikkhu</span>, que tendo ido esmolar alimentos num vilarejo ou
cidade, não ganha tanta comida comum ou refinada quanto necessita para se
satisfazer. O pensamento lhe ocorre 'Ao esmolar alimentos num vilarejo ou cidade,
não ganhei tanta comida comum ou refinada quanto necessito para me satisfazer.
Este meu corpo está leve e adequado para o trabalho. Porque não faço um esforço
para alcançar o que ainda não foi alcançado,<span style='mso-spacerun:yes'> 
</span>atingir o que não foi atingido, realizar o que ainda não foi realizado.'
Assim ele faz um esforço para alcançar o que ainda não foi alcançado,<span
style='mso-spacerun:yes'>  </span>atingir o que não foi atingido, realizar o
que ainda não foi realizado. Essa é a quinta razão para o estimulo da energia. <o:p></o:p></span></p>


<p class=Normal> 
&quot;Então existe o caso em que um <span
class=SpellE>bhikkhu</span>, que tendo ido esmolar alimentos num vilarejo ou
cidade, ganha tanta comida comum ou refinada quanto necessita para se
satisfazer.  O pensamento lhe ocorre: 'Ao esmolar alimentos num vilarejo ou
cidade,<span style='mso-spacerun:yes'>  </span>ganhei tanta comida comum ou
refinada<span style='mso-spacerun:yes'>  </span>quanto necessito para me
satisfazer. Este meu corpo está forte e adequado para o trabalho. Porque não
faço um esforço para alcançar o que ainda não foi alcançado, atingir o que não
foi atingido, realizar o que ainda não foi realizado.' Assim ele faz um esforço
para alcançar o que ainda não foi alcançado, atingir o que não foi atingido,
realizar o que ainda não foi realizado. Essa é a sexta razão para o estimulo da
energia. <o:p></o:p></span></p>


<p class=Normal> 
&quot;Então existe o caso em que um <span
class=SpellE>bhikkhu</span> está abatido por uma leve enfermidade. O pensamento lhe
ocorre: 'Eu estou abatido por uma leve enfermidade. Agora, existe a
possibilidade de que piore. Porque não faço um esforço antecipado para alcançar
o que ainda não foi alcançado, atingir o que não foi atingido, realizar o que
ainda não foi realizado.' Assim ele faz um esforço para alcançar o que ainda não
foi alcançado, atingir o que não foi atingido,<span style='mso-spacerun:yes'> 
</span>realizar o que ainda não foi realizado. Essa é a sétima razão para o
estimulo da energia<o:p></o:p></span></p>


<p class=Normal> &quot;Então existe o caso em que um <span class=SpellE>bhikkhu</span>
se recuperou de sua enfermidade, não muito depois de sua recuperação o
pensamento lhe ocorre: 'Eu me recuperei da minha enfermidade<span class=GramE>,.</span>
Não faz muito tempo da minha recuperação. Agora, existe a possibilidade de que a
enfermidade retorne. Porque não faço um esforço antecipado para alcançar o que
ainda não foi alcançado, atingir o que não foi atingido, realizar o que ainda
não foi realizado.' Assim ele faz um esforço para alcançar o que ainda não foi
alcançado, atingir o que não foi atingido, realizar o que ainda não foi
realizado. Essa é a oitava razão para o estimulo da energia<o:p></o:p></span></p>


