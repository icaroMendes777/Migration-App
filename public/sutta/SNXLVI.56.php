<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim ouvi.
Em certa ocasião, o Abençoado estava<span style="mso-spacerun: yes">  </span>em
Rajagaha na Montanha do Pico do Abutre. Então, o Príncipe Abhaya foi até o Abençoado
e depois de cumprimentá-lo sentou a um lado e disse: [</span><a href="#N1"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>1</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Venerável senhor, Purana Kassapa diz: ‘Não há causa e condição para a
falta de conhecimento e visão; a falta de conhecimento e visão ocorrem sem
causa e condição. Não há causa e condição para conhecimento e visão;
conhecimento e visão ocorrem sem causa e condição.’ O que o Abençoado diz
disso?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Príncipe,
há causa e condição para a falta de conhecimento e visão; a falta de
conhecimento e visão ocorre com causa e condição. Há causa e condição para
conhecimento e visão; conhecimento e visão ocorrem com causa e condição.”<o:p></o:p></span></p>



<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-language:PT-BR'>(I. A causa para falta de conhecimento e visão)<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas,
venerável senhor, qual é a causa e condição para a falta de conhecimento e
visão? De que modo a falta de conhecimento e visão tem causa e condição?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Numa
ocasião, príncipe, em que alguém permanece com a mente obcecada pelo desejo
sensual, subjugada pelo desejo sensual, e ele nem sabe e tampouco vê como na
verdade é a escapatória do desejo sensual que já surgiu: essa é a causa e
condição para a falta de conhecimento e visão; é desse modo que a falta de
conhecimento e visão tem causa e condição.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Novamente,
príncipe, numa ocasião em que alguém permanece com a mente obcecada pela má
vontade ... obcecada pela preguiça e torpor ... obcecada pela inquietação e
ansiedade ... obcecada pela dúvida,<span style="mso-spacerun: yes"> 
</span>subjugada pela dúvida, e ele nem sabe e tampouco vê como na verdade é a
escapatória da dúvida que já surgiu: essa é a causa e condição para a falta de
conhecimento e visão; é desse modo que a falta de conhecimento e visão tem
causa e condição.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Como é
chamada essa explicação do Dhamma, venerável senhor?” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Estes são
chamados de obstáculos, príncipe.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Com certeza
esses são obstáculos, Abençoado! Com certeza esses são obstáculos, Iluminado!
Alguém subjugado apenas por um único obstáculo seria incapaz de saber e ver as
coisas como elas na verdade são, sem falar daquele subjugado pelos cinco
obstáculos.<o:p></o:p></span></p>



<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-language:PT-BR'>(II. A causa do conhecimento e da visão)<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas,
venerável senhor, qual é a causa e condição para conhecimento e visão? De que
modo conhecimento e visão têm causa e condição?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Neste
caso, príncipe, um bhikkhu desenvolve o fator da iluminação da atenção plena,
que tem como base o afastamento, desapego e cessação, que amadurece no abandono.
Com uma mente que tenha desenvolvido o fator da iluminação da atenção plena ele
sabe e vê as coisas como elas na verdade são. Essa é uma causa para
conhecimento e visão; é desse modo que conhecimento e visão têm causa e
condição.<o:p></o:p></span></p>

<p class=Normal>...</p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Novamente,
príncipe, um bhikkhu desenvolve o fator da iluminação da equanimidade, que tem
como base o afastamento, desapego e cessação, que amadurece no abandono. Com
uma mente que tenha desenvolvido o fator da iluminação da equanimidade ele sabe
e vê as coisas como elas na verdade são. Essa é uma causa para o conhecimento e
visão; é desse modo que conhecimento e visão têm causa e condição.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Como é
chamada essa explicação do Dhamma, venerável senhor?” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Estes são
chamados de fatores da iluminação, príncipe.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Com
certeza esses são fatores da iluminação, Abençoado! Com certeza esses são
fatores da iluminação, Iluminado! Alguém que possuísse apenas um único fator da
iluminação saberia e veria as coisas como elas na verdade são, sem falar
daquele que possui os sete fatores da iluminação. A fadiga corporal e a fatiga
mental que experimentei ao subir a Montanha do Pico do Abutre foram
apaziguadas. <a name=R2></a>Eu penetrei o Dhamma.” [<a href="#N2">2</a>]<o:p></o:p></span></p>


