<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] Dighanakha era o sobrinho do Ven. Sariputta. Nesta ocasião Sariputta havia
se tornado bhikkhu fazia apenas duas semanas e ainda estava no estágio de
entrar na correnteza. [</span><a href="#R1"><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Retorna</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[2] MA afirma que Dighanakha era um partidário da aniquilação, (<i>ucchedavadin</i>),
e explica esta afirmação com o seguinte significado: “Nenhum (tipo de)
renascimento é admissível para mim.” No entanto, o texto em si não proporciona
nenhuma evidência concreta para suportar essa interpretação. Parece muito mais
provável que a afirmação de Dighanakha, “Nada é admissível para mim,” (<i>sabbam
me na khamati</i>), se aplica de forma específica a outras idéias filosóficas e
assim mostra que Dighanakha era um cético radical da categoria caracterizada de
forma irônica como “contorcendo-se como uma enguia” no </span><a
href="MN76.php#T30"><span lang=PT-BR style='mso-ansi-language:PT-BR'>MN 76.30</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>. A sua afirmativa equivale ao
repúdio geral de todas as idéias filosóficas.[</span><a href="#R2"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>Retorna</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=N3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[3] Esta conversa, conforme interpretado por MA e MT, pode ser
compreendida da seguinte forma: O Buda sugere, de acordo com a pergunta feita
por ele, que a afirmativa de Dighanakha traz uma contradição inerente. Pois ele
não poderia rejeitar tudo sem também rejeitar a sua própria idéia e isso
caracterizaria a posição oposta, isto é, que algo é admissível para ele. No
entanto, embora Dighanakha reconheça o sentido da pergunta do Buda, ele
continua a insistir na sua idéia de que nada é admissível para ele.[</span><a
href="#R3"><span lang=PT-BR style='mso-ansi-language:PT-BR'>Retorna</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=N4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[4] MA diz que a primeira sentença se refere aos que primeiro adotam
uma idéia básica eternalista ou de aniquilação e depois adotam variações
secundárias daquela idéia; a segunda sentença se refere aos que abandonam a
sua idéia básica sem adotar uma alternativa. Mas se, como parece
plausível,<span style='mso-spacerun:yes'>  </span>Dighanakha era um cético
radical, então a afirmação do Buda pode ser compreendida como apontando para
aquilo de insatisfatório inerente na posição adotada pelo cético: é
desconfortável sob o ponto de vista psicológico insistir em permanecer
desinformado. Por isso a maioria dos céticos, enquanto professam a rejeição de
todas as idéias, de forma sub-reptícia adotam uma idéia definida, enquanto que
uns poucos abandonam o ceticismo para buscar um caminho de auto-conhecimento.[</span><a
href="#R4"><span lang=PT-BR style='mso-ansi-language:PT-BR'>Retorna</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>]</span><span lang=PT-BR
style='font-size:11.0pt;mso-bidi-font-size:12.0pt;mso-ansi-language:PT-BR'><o:p></o:p></span></p>


<p class=Normal><a name=N5></a> [5] </span><span lang=PT-BR
style='mso-ansi-language:PT-BR'>MA identifica as três idéias neste caso como eternidade,
aniquilação e eternidade parcial. O entendimento eternalista está próximo da
cobiça, (<i>saragaya santike</i>), etc., porque afirma e se delicia com a
existência, em uma forma no entanto sublimada; a aniquilação está próxima da
não cobiça, etc., porque embora tenha uma concepção errada do eu, conduz ao
desencantamento com a existência. Se o segundo entendimento for compreendido
como ceticismo radical, isso também poderá ser visto como próximo da não
cobiça, expressando a desilusão com a tentativa de suportar o apego à
existência com um fundamento teórico,<span style='mso-spacerun:yes'>  </span>e
dessa forma representando um passo tentativo, embora equivocado, na direção do
desapego. [</span><a href="#R5"><span lang=PT-BR style='mso-ansi-language:PT-BR'>Retorna</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=N6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[6] MA: Este ensinamento é apresentado para mostrar a Dighanakha o
perigo da sua idéia e assim encorajá-lo a descartá-la.[</span><a href="#R6"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>Retorna</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=N7></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[7] MA: Neste ponto Dighanakha descartou a sua idéia de aniquilação.
Assim, o Buda agora lhe ensina a meditação de insight, primeiro através da
impermanência do corpo e através da impermanência dos fatores mentais
