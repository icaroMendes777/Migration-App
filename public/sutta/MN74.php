<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião, o Abençoado estava em Rajagaha na Caverna do Javali no
Pico do Abutre.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. Então, o
errante Dighanakha foi até o Abençoado e ambos se cumprimentaram. [</span><a
href="#N1"><span lang=PT-BR style='mso-ansi-language:PT-BR'>1</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R1></a>Quando a conversa
amigável e cortês havia terminado, ele ficou em pé a um lado e disse para o
Abençoado: “Mestre Gotama, assim é a minha doutrina e entendimento : ‘Nada é
admissível para mim.’” [</span><a href="#N2"><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Essa sua idéia, Aggivessana, ‘Nada é admissível para mim.’ – nem ao
menos essa idéia é admissível para você?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se essa
minha idéia fosse admissível para mim, Mestre Gotama, isso também seria a mesma
coisa, isso também seria a mesma coisa.” [</span><a href="#N3"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>3</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=R3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>3. “Bem, Aggivessana, há muitos no mundo que dizem: ‘Isso também seria a
mesma coisa, isso também seria a mesma coisa’ mas eles não abandonam aquela
idéia e tomam mais uma outra idéia. São poucos no mundo que dizem: ‘Isso também
seria a mesma coisa, isso também seria a mesma coisa’ e que abandonam aquela
idéia e não tomam uma outra idéia. [</span><a href="#N4"><span lang=PT-BR
style='mso-ansi-language:PT-BR'>4</span></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=R4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>4. “Aggivessana, existem alguns contemplativos e brâmanes cuja doutrina
e entendimento é: ‘Tudo é admissível para<span style='mso-spacerun:yes'> 
</span>mim.’ Existem alguns contemplativos e brâmanes cuja doutrina e
entendimento é: ‘Nada é admissível para<span style='mso-spacerun:yes'>  </span>mim.’
E existem alguns contemplativos e brâmanes cuja doutrina e entendimento é:
‘Algumas coisas são admissíveis para<span style='mso-spacerun:yes'> 
</span>mim, algumas coisas não são admissíveis para mim.’[</span><a href="#N5"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>5</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>] <a name=R5></a>Dentre estes, o entendimento
daqueles contemplativos e brâmanes, cuja doutrina e entendimento é ‘Tudo é
admissível para<span style='mso-spacerun:yes'>  </span>mim,’ está próximo da
cobiça, próximo do cativeiro, próximo do deleite, próximo da agarração, próximo
do apego. O entendimento daqueles contemplativos e brâmanes, cuja doutrina e
entendimento é ‘Nada é admissível para<span style='mso-spacerun:yes'> 
</span>mim,’ está próximo da não-cobiça, próximo do não-cativeiro, próximo do
não-deleite, próximo da não-agarração, próximo do não-apego.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. Quando
isso foi dito, o errante Dighanakha observou: “Mestre Gotama recomenda a minha
idéia, Mestre Gotama recomenda a minha idéia.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aggivessana,
quanto aos<span style='mso-spacerun:yes'>  </span>contemplativos e brâmanes
cuja doutrina e entendimento é ‘Algumas coisas são admissíveis para<span
style='mso-spacerun:yes'>  </span>mim, algumas coisas não são admissíveis para mim,’ -
o entendimento deles quanto ao que é admissível está próximo da cobiça, próximo
do cativeiro, próximo do deleite, próximo da agarração, próximo do apego,
enquanto que o entendimento deles quanto ao que não é admissível está próximo
da não-cobiça, próximo do não-cativeiro, próximo do não-deleite, próximo da
não-agarração, próximo do não-apego.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. “Agora,
Aggivessana, um homem sábio, entre aqueles contemplativos e brâmanes, cuja
doutrina e entendimento é ‘Tudo é admissível para<span
style='mso-spacerun:yes'>  </span>mim’ considera o seguinte:[</span><a
href="#N6"><span lang=PT-BR style='mso-ansi-language:PT-BR'>6</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R6></a>‘Se eu agarrar com
obstinação a minha idéia “Tudo é admissível para<span
style='mso-spacerun:yes'>  </span>mim” e declarar: “Somente isso é verdadeiro,
todo o restante é falso,” então poderei ter uma desavença com os outros dois:
com um contemplativo ou brâmane cuja doutrina e entendimento é “Nada é
admissível para<span style='mso-spacerun:yes'>  </span>mim” e com um
contemplativo ou brâmane cuja doutrina e entendimento é “Algumas coisas são
admissíveis para<span style='mso-spacerun:yes'>  </span>mim, algumas coisas não
são admissíveis para mim.” Eu poderei ter uma desavença com esses dois, e
quando há uma desavença, há disputas; quando há disputas, há brigas; quando há
brigas, há irritação.’ Antevendo desavenças, disputas, brigas e irritação, ele
abandona aquela idéia e não toma nenhuma outra idéia. Assim é como ocorre o
abandono daquelas idéias; assim é como ocorre a abdicação daquelas idéias.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. “Um
homem sábio, entre aqueles contemplativos e brâmanes, cuja doutrina e
entendimento é ‘Nada é admissível para<span style='mso-spacerun:yes'> 
</span>mim’ considera o seguinte: ‘Se eu agarrar com obstinação a minha idéia
“Nada é admissível para<span style='mso-spacerun:yes'>  </span>mim” e declarar:
“Somente isso é verdadeiro, todo o restante é falso,” então poderei ter uma
desavença com os outros dois: com um contemplativo ou brâmane cuja doutrina e
entendimento é “Tudo é admissível para<span style='mso-spacerun:yes'> 
</span>mim” e com um contemplativo ou brâmane cuja doutrina e entendimento é
“Algumas coisas são admissíveis para<span style='mso-spacerun:yes'> 
</span>mim, algumas coisas não são admissíveis para mim.” Eu poderei ter uma
desavença com esses dois e quando há uma desavença, há disputas; quando há
disputas, há brigas; quando há brigas, há irritação.’ Antevendo desavenças,
disputas, brigas e irritação, ele abandona aquela idéia e não toma nenhuma
outra idéia. Assim é como ocorre o abandono daquelas idéias; assim é como
ocorre a abdicação daquelas idéias.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. “Um
homem sábio, entre aqueles contemplativos e brâmanes, cuja doutrina e
entendimento é ‘Algumas coisas são admissíveis para<span
style='mso-spacerun:yes'>  </span>mim, algumas coisas não são admissíveis para
mim’ considera o seguinte: ‘Se eu agarrar com obstinação a minha idéia “Algumas
coisas são admissíveis para<span style='mso-spacerun:yes'>  </span>mim, algumas
coisas não são admissíveis para mim” e declarar: “Somente isso é verdadeiro,
todo o restante é falso,” então poderei ter uma desavença com os outros dois:
com um contemplativo ou brâmane cuja doutrina e entendimento é “Tudo é admissível
para<span style='mso-spacerun:yes'>  </span>mim” e com um contemplativo ou
brâmane cuja doutrina e entendimento é “Nada é admissível para<span
style='mso-spacerun:yes'>  </span>mim.” Eu poderei ter uma desavença com esses
dois, e quando há uma desavença, há disputas; quando há disputas, há brigas;
quando há brigas, há irritação.’ Antevendo desavenças, disputas, brigas e
irritação, ele abandona aquela idéia e não toma nenhuma outra idéia. Assim é
como ocorre o abandono daquelas idéias; assim é como ocorre a abdicação
daquelas idéias.<o:p></o:p></span></p>



<p class=Normal><a name=T9></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>9. “Agora, Aggivessana,[</span><a href="#N7"><span lang=PT-BR
style='mso-ansi-language:PT-BR'>7</span></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>] <a name=R7></a>este corpo feito de forma material, consistindo dos
quatro grandes elementos, procriado por uma mãe e pai, construído com arroz
cozido e mingau, está sujeito à impermanência, a ser gasto e pulverizado, à
dissolução e desintegração. Ele deve ser considerado como impermanente, como
sofrimento, como uma enfermidade, como um tumor, como uma flecha, como uma
calamidade, como uma aflição, como um estranho, desintegrando, vazio, não-eu.
Quando alguém considera este corpo assim, ele abandona o desejo pelo corpo, a
afeição pelo corpo, a subserviência ao corpo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10.
“Existem, Aggivessana, três tipos de sensações: sensação prazerosa, sensação
dolorosa e sensação nem dolorosa, nem prazerosa. Quando uma pessoa sente uma
sensação prazerosa, ela não sente uma sensação dolorosa ou uma sensação nem
dolorosa, nem prazerosa; naquela ocasião ela sente apenas a sensação
prazerosa.<span style='mso-spacerun:yes'>  </span>Quando uma pessoa sente uma
sensação dolorosa, ela não sente uma sensação prazerosa ou uma sensação nem
dolorosa, nem prazerosa; naquela ocasião ela sente apenas a sensação dolorosa.
Quando uma pessoa sente uma sensação nem dolorosa, nem prazerosa, ela não sente
uma sensação dolorosa ou uma sensação prazerosa; naquela ocasião ela sente
apenas a sensação nem dolorosa, nem prazerosa.<o:p></o:p></span></p>



<p class=Normal><a name=T11></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>11. “A sensação prazerosa, Aggivessana, é impermanente, condicionada,
com origem dependente, sujeita à destruição, desaparecimento, decadência e
cessação. A sensação dolorosa também é impermanente, condicionada, com origem
dependente, sujeita à destruição, desaparecimento, decadência e cessação. A
sensação nem dolorosa, nem prazerosa também é impermanente, condicionada, com
origem dependente, sujeita à destruição, desaparecimento, decadência e
cessação.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. “Vendo
dessa forma, um nobre discípulo bem instruído se desencanta com a sensação
prazerosa, se desencanta com a sensação dolorosa, se desencanta com a sensação
nem dolorosa, nem prazerosa. Desencantado, ele se torna desapegado. Através do
desapego a sua mente é libertada. Quando ela está libertada surge o conhecimento:
‘Libertada.’ Ele compreende que: ‘O nascimento foi destruído, a vida santa foi
vivida, o que deveria ser feito foi feito, não há mais vir a ser a nenhum
estado.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. “Um
bhikkhu cuja mente esteja assim libertada, Aggivessana, não toma partido de
ninguém e não disputa com ninguém; ele emprega a linguagem usada de forma
corrente no mundo sem se agarrar a ela.” [</span><a href="#N8"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>8</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=R8></a><a name=T14></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>14. Agora, naquela ocasião o venerável
Sariputta estava em pé atrás do Abençoado,<span style='mso-spacerun:yes'> 
</span>ventilando-o. Então ele pensou “O Abençoado, de fato, fala do abandono
dessas coisas através do conhecimento direto; o Iluminado, de fato, fala da
abdicação dessas coisas através do conhecimento direto.” Enquanto o venerável
Sariputta considerava isso, através do desapego a sua mente foi libertada das
impurezas. [</span><a href="#N9"><span lang=PT-BR style='mso-ansi-language:
PT-BR'>9</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=R9></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>15. Mas no errante Dighanakha surgiu a perfeita e imaculada visão do
Dhamma: “Tudo que está sujeito ao surgimento está sujeito à cessação.” O
errante Dighanakha viu o Dhamma, alcançou o Dhamma, compreendeu o Dhamma,
examinou a fundo o Dhamma; ele superou a dúvida, se libertou da perplexidade,
conquistou a intrepidez, e se tornou independente dos outros na Revelação do
Mestre. [</span><a href="#N10"><span lang=PT-BR style='mso-ansi-language:PT-BR'>10</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=R10></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>16. Então ele disse para o Abençoado: “Magnífico, Mestre Gotama!
Magnífico, Mestre Gotama! Mestre Gotama esclareceu o Dhamma de várias formas,
como se tivesse colocado em pé o que estava de cabeça para baixo, revelasse o
que estava escondido, mostrasse o caminho para alguém que estava perdido ou
segurasse uma lâmpada no escuro para que aqueles que possuem visão pudessem
ver as formas. Eu busco refúgio no Mestre Gotama, no Dhamma e na Sangha dos
bhikkhus. Que o Mestre Gotama me aceite como o discípulo leigo que 
buscou refúgio para o resto da vida.”<span style='mso-spacerun:yes'> 
</span><o:p></o:p></span></p>

