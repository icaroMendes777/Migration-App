<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. Então, ao amanhecer, a bhikkhuni Soma se vestiu e tomando a tigela e
o manto externo foi para Savatthi esmolar alimentos. Depois de haver esmolado
alimentos em Savatthi e de haver retornado, após a refeição, ela se dirigiu ao
bosque dos Homens Cegos em busca de isolamento. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, Mara, o Senhor do
Mal, desejando despertar medo, trepidação e terror na bhikkhuni Soma, desejando
que ela abandonasse a sua concentração, se aproximou e se dirigiu a ela em
versos: <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
 “Aquele estado tão difícil de ser alcançado<br>
que é realizado pelos sábios,<br>
não pode ser alcançado por uma mulher<br>
com os seus dois dedos de sabedoria.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Foi então que a bhikkhuni
Soma pensou: “Agora, quem será que recitou esses versos – um ser humano ou um
ser não humano?” Então lhe ocorreu: “Foi Mara, o Senhor do Mal, quem recitou
esses versos desejando despertar medo, trepidação e terror em mim, desejando
que eu abandone a minha concentração.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então a bhikkhuni Soma,
tendo compreendido, “Este é Mara, o Senhor do Mal,” respondeu em versos: <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
 “Que diferença faz a condição feminina<br>
quando a mente está bem concentrada,<br>
quando o conhecimento flui com segurança,<br>
vendo claramente, corretamente,<br>
de acordo com o Dhamma. <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
Qualquer um que pensar<br>
’Eu sou uma mulher’ ou ‘Eu sou um homem’<br>
ou ‘ Afinal, sou eu alguma coisa? ’ -<span style="mso-spacerun: yes">  </span><br>
é a esse que Mara deve se dirigir.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, Mara, o Senhor do
Mal, compreendendo que, “A bhikkhuni Soma sabe quem sou,” triste e desapontado,
desapareceu no mesmo instante. <o:p></o:p></span></p>



