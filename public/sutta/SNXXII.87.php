<p class=Normal> Assim ouvi. Em certa ocasião o Abençoado estava em Rajagaha no Bambual, no Santuário dos Esquilos. </p>

<p class=Normal> Agora, naquela ocasião o venerável Vakkali estava atormentado, sofrendo e gravemente enfermo. Então, ele se dirigiu aos seus assistentes desta forma: "Venham, amigos, vão até o Abençoado, homenageiem-no em meu nome com a sua cabeça aos pés dele e digam: 'Venerável senhor, o venerável Vakkali está atormentado, sofrendo e gravemente enfermo; ele homenageia o Abençoado com a cabeça aos seus pés.' Depois diga: 'Seria bom, venerável senhor, se o Abençoado fosse até a residência do venerável Vakkali, por compaixão.'"</p>

<p class=Normal> "Sim amigo," os bhikkhus responderam, e foram até onde estava o Abençoado, após homenageá-lo, eles sentaram a um lado e relataram a sua mensagem. O Abençoado consentiu em silêncio. </p>

<p class=Normal> Então o Abençoado se vestiu e tomando a tigela e o manto externo, foi para a residência do venerável Vakkali. O venerável Vakkali  viu o Abençoado vindo à distância e ficou agitado na cama. O Abençoado disse: "Já basta, Vakkali, não fique agitado na cama. Este assento está preparado. Eu sentarei aqui." </p>

<p class=Normal> O Abençoado sentou no assento que havia sido preparado e disse para o venerável Vakkali: "Eu espero que você esteja melhorando, espero que você esteja confortável, espero que as suas sensações de dor estejam diminuindo e não aumentando e que a sua diminuição, não o seu aumento, seja evidente." </p>

<p class=Normal> "Venerável senhor, eu não estou melhorando, não me sinto confortável. Minhas sensações de dor estão aumentando, não diminuindo, o seu aumento, não a sua diminuição é evidente." </p>

<p class=Normal> "Eu espero então, Vakkali, que você não esteja atormentado pelo remorso e pelo arrependimento." </p>

<p class=Normal> "De fato, venerável senhor, eu sinto muito remorso e muito arrependimento." </p>

<p class=Normal> "Eu espero, Vakkali, que você não tenha nada que se repreender com relação à virtude." </p>

<p class=Normal> "Venerável senhor, eu não tenho nada que me repreender com relação à virtude." </p>

<p class=Normal> "Então, Vakkali, se você não tem nada que se repreender com relação à virtude, porque você está atormentado pelo remorso e arrependimento?" </p>

<p class=Normal> "Por muito tempo, venerável senhor, eu queria ter vindo ver o Abençoado, mas não estive em condições de fazer isso." </p>

<p class=Normal> "Já basta, Vakkali! Porque você quer ver este corpo asqueroso? Quem vê o Dhamma vê a mim; quem me vê, vê o Dhamma. Pois ao ver o Dhamma, Vakkali, a pessoa vê a mim; e vendo a mim, ela vê o Dhamma. </p>

<p class=Normal> “O que você pensa, Vakkali, a forma é permanente ou impermanente?" - “Impermanente, venerável senhor.” – “Aquilo que é impermanente é sofrimento ou felicidade?” – “Sofrimento, venerável senhor.” – “É adequado que aquilo que é impermanente, sofrimento e está sujeito à mudança, seja considerado desta forma: ‘Isso é meu, isso sou eu, isso é o meu eu’?” – “Não venerável senhor.” </p>

<p class=Normal> “Vakkali, o que você pensa: A sensação ... percepção ... formações volitivas ... consciência são permanentes ou impermanentes?” – “Impermanentes venerável senhor.” - “Aquilo que é impermanente é sofrimento ou felicidade?” – “Sofrimento, venerável senhor.” – “É adequado que aquilo que é impermanente, sofrimento e está sujeito à mudança, seja considerado desta forma: ‘Isso é meu, isso sou eu, isso é o meu eu’?” – “Não venerável senhor.” </p>

<p class=Normal> Portanto, Vakkali, qualquer tipo de forma material, quer seja do passado, do futuro ou do presente ... toda forma material deve ser vista como na verdade ela é, com correta sabedoria, deste modo: ‘Isso não é meu, isso não sou eu, isso não é o meu eu’. Qualquer tipo de sensação ... Qualquer tipo de percepção ... Qualquer tipo de formações volitivas ... Qualquer tipo de consciência ... toda consciência deve ser vista como na verdade ela é, com correta sabedoria, deste modo: ‘Isso não é meu, isso não sou eu, isso não é o meu eu.’ </p>

<p class=Normal> “Vendo desse modo um nobre discípulo bem instruído se desencanta com a forma material, se desencanta com a sensação, se desencanta com a percepção, se desencanta com as formações volitivas, se desencanta com a consciência. </p>

<p class=Normal> “Desencantado, ele se torna desapegado. Através do desapego a sua mente é libertada. Quando ela está libertada surge o conhecimento: ‘Libertada.’ Ele compreende que: ‘O nascimento foi destruído, a vida santa foi vivida, o que deveria ser feito foi feito, não há mais vir a ser a nenhum estado’. </p>

<p class=Normal>Então o Abençoado depois de dar essa exortação ao venerável Vakkali levantou do seu assento e partiu para o Pico do Abutre. </p>

<p class=Normal> Então, não muito tempo depois do Abençoado ter partido, o venerável Vakkali se dirigiu aos seus assistentes assim: "Venham amigos, levantem a minha cama e me carreguem até a Rocha Negra na encosta do Isigili. Como pode alguém como eu pensar em morrer no meio das casas?" </p>

<p class=Normal> "Sim, amigo," aqueles bhikkhus responderam e levantaram a cama do venerável Vakkali e o carregaram até a Rocha Negra na encosta do Isigili. </p>

<p class=Normal> O Abençoado passou o resto do dia e a noite no pico do Abutre. Então, quando a noite estava bem avançada, duas devatas com belíssima aparência que iluminaram todo o pico do Abutre, se aproximaram do Abençoado. Ao se aproximar elas homenagearam o Abençoado e ficando em pé a um lado uma devata disse: "Venerável senhor, o bhikkhu Vakkali intenciona pela libertação." A outra devata disse: "Com certeza, venerável senhor, ele será libertado como alguém bem libertado." Isso foi o que as devatas disseram. Tendo dito isso as devatas homenagearam o Abençoado, e mantendo-o à sua direita, desapareceram. </p>

<p class=Normal> Então quando a noite havia terminado, o Abençoado se dirigiu aos bhikkhus assim: "Venham, bhikkhus, vão até o bhikkhu Vakkali e digam: 'Amigo Vakkali, ouça as palavras do Abençoado e de duas devatas. Na noite passada, amigo, duas devatas com belíssima aparência se aproximaram do Abençoado. Uma devata disse: "Venerável senhor, o bhikkhu Vakkali intenciona pela libertação." A outra devata disse: "Com certeza, venerável senhor, ele será libertado como alguém bem libertado." E o Abençoado lhe diz, amigo Vakkali: "Não tenha medo, Vakkali, não tenha medo! A sua morte não será ruim. O seu falecimento não será ruim."'" </p>

<p class=Normal> "Sim, venerável senhor, aqueles bhikkhus responderam e foram até o venerável Vakkali e lhe disseram: "Amigo Vakkali, ouça as palavras do Abençoado e de duas devatas." </p>

<p class=Normal> Então o venerável Vakkali disse para os seus assistentes: "Venham, amigos, ajudem-me a descer da cama. Como pode alguém como eu pensar em ouvir o ensinamento do Abençoado sentado sobre um assento elevado." </p>

<p class=Normal> "Sim, amigo," aqueles bhikkhus responderam e eles ajudaram o venerável Vakkali a descer da cama. </p>

<p class=Normal> "Na noite passada, amigo, duas devatas com belíssima aparência se aproximaram do Abençoado. Uma devata disse: 'Venerável senhor, o bhikkhu Makkali intenciona pela libertação.' A outra devata disse: 'Com certeza, venerável senhor, ele será libertado como alguém bem libertado.' E o Abençoado lhe diz, amigo Vakkali: 'Não tenha medo, Vakkali, não tenha medo! A sua morte não será ruim. O seu falecimento não será ruim.'"</p>

<p class=Normal> "Muito bem, então amigos homenageiem o Abençoado em meu nome com a sua cabeça aos pés dele e digam: 'Venerável senhor, o venerável Vakkali está atormentado, sofrendo e gravemente enfermo; ele homenageia o Abençoado com a cabeça aos seus pés.' Depois digam: 'A forma é impermanente: eu não tenho perplexidade com relação a isso, venerável senhor, eu não tenho dúvida que tudo que é impermanente é sofrimento. Eu não tenho dúvida que eu não tenho mais desejo, cobiça, ou afeição em relação ao que é impermanente, sofrimento, e sujeito à mudança. A sensação é impermanente ... a percepção é impermanente ... as formações volitivas são impermanentes ... a consciência  é impermanente: eu não tenho perplexidade com relação a isso, venerável senhor, eu não tenho dúvida que tudo que é impermanente é sofrimento. Eu não tenho dúvida que eu não tenho mais desejo, cobiça, ou afeição em relação ao que é impermanente, sofrimento, e sujeito à mudança.'"</p>

<p class=Normal> "Sim, amigo," aqueles bhikkhus responderam e então partiram. Então não muito tempo depois de haverem partido, o venerável Vakkali usou a faca. </p>

<p class=Normal> Então aqueles bhikkhus foram até o Abençoado e relataram a mensagem. O Abençoado então se dirigiu aos bhikkhus: "Venham, bhikkhus, vamos até a Rocha Negra na encosta do Isigili onde Vakkali usou a faca." </p>

<p class=Normal> "Sim, venerável senhor," os bhikkhus responderam. Então o Abençoado com um grupo de bhikkhus foram até a Rocha Negra na encosta do Isigili. O Abençoado viu à distância o venerável Vakkali deitado na cama com os ombros virados. </p>

<p class=Normal> Agora, naquela ocasião uma nuvem de fumaça, um redemoinho de trevas, estava se movendo para o leste, depois para o oeste, para o norte, para o sul, para cima, para baixo e para os quadrantes intermediários. Então, o Abençoado se dirigiu aos bhikkhus: “Vocês vêm, bhikkhus, aquela nuvem de fumaça, um redemoinho de trevas, movendo-se para o leste, depois para o oeste, para o norte, para o sul, para cima, para baixo e para os quadrantes intermediários?" </p>

<p class=Normal> “Sim, venerável senhor.” </p>

<p class=Normal> “Aquilo, bhikkhus, é Mara, o Senhor do Mal, procurando pela consciência de Vakkali, perguntando a si mesmo: ‘Onde agora se estabeleceu a consciência de Vakkali?‘ No entanto, bhikkhus, com a consciência não estabelecida, Vakkali realizou o <i>parinibbana</i>.” </p>


