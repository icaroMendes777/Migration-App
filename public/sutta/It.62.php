<p class=Normal> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: “Bhikkhus, há essas três faculdades. Quais três? A faculdade da segurança: 'Eu finalmente terei o conhecimento daquilo que ainda não é conhecido'; a faculdade do conhecimento último; e a faculdade daquele que tem o conhecimento último. [<a href="#N1">1</a>] Essas, bhikkhus, são as três faculdades."</p>

<p class=Normal style='margin-left:.75in'> Para um treinando que pratica<br>
em conformidade com o caminho direto, <br>
o conhecimento da destruição surge primeiro<br>
seguido de imediato pelo conhecimento último.</p>

<p class=Normal style='margin-left:.75in'> Libertado através do conhecimento último, <br>
destruindo os grilhões de ser/existir<br>
o pacificado tem a certeza: <br>
"Inabalável é a minha libertação."</p>

<p class=Normal style='margin-left:.75in'>Dotado dessa faculdade, <br>
o pacificado se deleita no estado pacífico, <br>
tendo conquistado Mara com a sua horda, <br>
ele suporta seu último corpo.</p>


