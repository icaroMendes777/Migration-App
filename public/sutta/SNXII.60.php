<p class=Normal> Certa ocasião o Abençoado estava entre os Kurus em uma cidade denominada Kammasadhamma, então o Venerável Ananda foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse: </p>

<p class=Normal> “É maravilhoso e admirável, venerável senhor, que essa origem dependente seja tão profunda e difícil de ser vista, no entanto para mim ela parece tão simples, clara como a luz do dia.” </p>

<p class=Normal> “Não diga isso, Ananda! Não diga isso, Ananda! Esta origem dependente é um ensinamento profundo, difícil de ser visto. É por não entender, não compreender e não penetrar de forma completa este ensinamento que os seres ficam confusos como um novelo embaraçado, como uma corda embolada cheia de nós, como juncos enredados, e não conseguem escapar da transmigração, dos planos de miséria, dos destinos ruins, dos mundos inferiores. </p>

<p class=Normal>"Ananda, quando alguém permanece contemplando a gratificação nas coisas que podem ser apegadas, o desejo aumenta. Com o desejo como condição, o apego surge; com o apego como condição, o ser/existir surge; com o ser/existir como condição, o nascimento surge; com o nascimento como condição, o envelhecimento e morte, lamentação, dor, angústia e desespero surgem. Essa é a origem de toda essa massa de sofrimento. </p>

<p class=Normal>"Suponha, Ananda, que houvesse uma grande árvore e todas as suas raízes espalhadas pela terra enviassem a seiva para cima. Sustentada por essa seiva, alimentada por ela, essa grande árvore se manteria em pé por muito tempo. Do mesmo modo, quando alguém permanece contemplando a gratificação nas coisas que podem ser apegadas, o desejo aumenta ... Essa é a origem de toda essa massa de sofrimento. </p>

<p class=Normal>"Ananda, quando alguém permanece contemplando o perigo nas coisas que podem ser apegadas, o desejo cessa. Com a cessação do desejo, o apego cessa; com a cessação do apego, o ser/existir cessa; com a cessação do ser/existir, o nascimento cessa; com a cessação do nascimento, o envelhecimento e morte, lamentação, dor, angústia e desespero cessam. Essa é a cessação de toda essa massa de sofrimento. </p>

<p class=Normal>"Suponha, Ananda, que houvesse uma grande árvore. Então um homem viesse trazendo uma pá e um cesto. Ele cortaria a árvore pela raiz, cavaria e arrancaria todas as raízes, até mesmo as mais delgadas. Ele cortaria a árvore em pedaços, partiria os pedaços em pedaços menores e os reduziria a lascas. Depois ele secaria as lascas ao vento e ao sol, queimaria essas lascas no fogo e recolheria as cinzas. Tendo feito isso, ele deixaria as cinzas serem levadas pelo vento ou as jogaria na correnteza de um rio. Assim, aquela grande árvore teria sido cortada pela raiz, feita como um tronco de palmeira, eliminada de tal forma que não estará mais sujeita a um a um futuro surgimento. Do mesmo modo, quando alguém permanece contemplando o perigo nas coisas que podem ser apegadas, o desejo cessa ... Essa é a cessação de toda essa massa de sofrimento." </p>

