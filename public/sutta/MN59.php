<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no
Parque de Anathapindika.<o:p></o:p></span></p>


<p class=Normal>2. Então o carpinteiro Pancakanga [<a href="#N1">1</a>] <a
name=R1></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>foi até o
venerável Udayin e depois de cumprimentá-lo sentou a um lado e perguntou</span>:</p>



<p class=Normal>3. “Venerável senhor, quantos tipos de sensações foram
declaradas pelo Abençoado?”</p>



<p class=Normal>“Três tipos de sensações foram declaradas pelo Abençoado,
chefe de família: sensações prazerosas, sensações dolorosas e sensações nem
dolorosas, nem prazerosas. Esses três tipos de sensações foram declaradas pelo
Abençoado.”</p>



<p class=Normal>“Não foram três tipos de sensações declaradas pelo
Abençoado, venerável Udayin; dois tipos de sensações foram declaradas pelo
Abençoado: sensações prazerosas e sensações dolorosas. Essa sensação nem
dolorosa, nem prazerosa foi<span style="mso-spacerun: yes">  </span>declarada
pelo Abençoado como um tipo de sensação pacífica e sublime.”</p>



<p class=Normal>Uma segunda vez e uma terceira vez o venerável Udayin
declarou a sua posição, e uma segunda e terceira vez o carpinteiro Pancakanga
declarou a dele. Mas o venerável Udayin não pôde convencer o carpinteiro Pancakanga
nem o carpinteiro Pancakanga pôde convencer o venerável Udayin.</p>



<p class=Normal>4. O venerável Ananda ouviu a conversa deles. Então ele foi
até o Abençoado e depois de <span lang=PT-BR style='mso-ansi-language:PT-BR'>cumprimentá-lo
sentou a um lado </span>e relatou toda a conversa entre o venerável Udayin e o
carpinteiro Pancakanga. Quando ele <span lang=PT-BR style='mso-ansi-language:
PT-BR'>ele terminou, o Abençoado disse para o venerável Ananda</span>:</p>



<p class=Normal>5. “<span lang=PT-BR style='mso-ansi-language:PT-BR'>Ananda,
a apresentação de Udayin que o carpinteiro Pancakanga não quis aceitar foi de
fato verdadeira e a apresentação do carpinteiro Pancakanga que Udayin não quis
aceitar foi de fato verdadeira. Eu declarei dois tipos de sensações numa
apresentação; eu declarei três tipos de sensações em outra apresentação; eu
declarei cinco tipos de sensações em outra apresentação; eu declarei seis tipos
de sensações em outra apresentação; eu declarei dezoito tipos de sensações em
outra apresentação; eu declarei trinta e seis tipos de sensações em outra
apresentação; eu declarei cento e oito tipos de sensações em outra apresentação</span>.[<a
href="#N2">2</a>] <a name=R2></a>Assim é como o Dhamma foi mostrado por mim em
diferentes apresentações.</p>



<p class=Normal>“<span lang=PT-BR style='mso-ansi-language:PT-BR'>Como o
Dhamma foi assim mostrado por mim em diferentes apresentações, é de se esperar
que aqueles que não admitem, permitem, nem aceitam aquilo que está bem
declarado e bem dito pelos outros, se envolvam em rixas, brigas e discussões e
apunhalem uns aos outros usando as palavras como adagas. Mas é de se esperar
que aqueles que admitem, permitem e aceitam aquilo que está bem declarado e bem
dito pelos outros, vivam em concórdia, com apreço mútuo, sem disputas,
mesclando como leite e água, vendo um ao outro com bondade</span>.</p>



<p class=Normal>6. “<span lang=PT-BR style='mso-ansi-language:PT-BR'>Ananda,
existem esses cinco elementos do prazer sensual. Quais cinco? Formas percebidas
pelo olho que são desejáveis, agradáveis e fáceis de serem gostadas, conectadas
com o desejo sensual e que provocam a cobiça. Sons percebidos pelo
ouvido ... Aromas percebidos pelo nariz ... Sabores percebidos pela língua ... Tangíveis
percebidos pelo corpo que são desejáveis, agradáveis e fáceis de serem
gostados, conectados com o desejo sensual e que provocam a cobiça. Agora o
prazer e a alegria que surgem na dependência desses elementos do prazer sensual
são chamados de prazer sensual</span>.</p>



<p class=Normal>7. “<span lang=PT-BR style='mso-ansi-language:PT-BR'>Se
alguém dissesse: ‘Esse é o máximo em prazer e alegria que os seres experimentam,’
eu não admitiria isso. Por que não? Porque existe um outro tipo de prazer mais
elevado e mais sublime do que aquele prazer. E qual é esse outro tipo de
prazer? Aqui, Ananda, um bhikkhu afastado dos prazeres sensuais, afastado das qualidades não hábeis, entra e permanece no primeiro jhana, que é caracterizado pelo pensamento aplicado e sustentado, com o êxtase e felicidade nascidos do afastamento. Esse é aquele outro tipo de prazer mais elevado e mais sublime que
o anterior</span>.</p>



<p class=Normal>8. “<span lang=PT-BR style='mso-ansi-language:PT-BR'>Se
alguém dissesse: ‘Esse é o máximo em prazer e alegria que os seres
experimentam,’ eu não admitiria isso. Por que não? Porque existe um outro tipo
de prazer mais elevado e mais sublime do que aquele prazer. E qual é esse outro
tipo de prazer? Aqui, Ananda, abandonando o pensamento aplicado e sustentado, um bhikkhu entra e permanece no segundo jhana, que é caracterizado pela segurança interna e perfeita unicidade da mente, sem o pensamento aplicado e sustentado, com o êxtase e felicidade nascidos da concentração. Esse é aquele outro tipo de
prazer mais elevado e mais sublime que o anterior</span>.</p>



<p class=Normal>9. “<span lang=PT-BR style='mso-ansi-language:PT-BR'>Se
alguém dissesse ... E qual é esse outro tipo de prazer? Aqui, Ananda, abandonando o êxtase, um bhikkhu entra e permanece no terceiro jhana que é caracterizado pela felicidade sem o êxtase, acompanhada pela atenção plena, plena consciência e equanimidade, acerca do qual os nobres declaram: ‘Ele permanece numa estada feliz, equânime e plenamente atento.’ Esse é aquele outro tipo de prazer mais
elevado e mais sublime que o anterior</span>.</p>



<p class=Normal>10. “<span lang=PT-BR style='mso-ansi-language:PT-BR'>Se alguém
dissesse ... E qual é esse outro tipo de prazer? Aqui, Ananda, com o completo desaparecimento da felicidade, um bhikkhu entra e permanece no quarto jhana, que possui nem felicidade nem sofrimento, com a atenção plena e a equanimidade purificadas.
Esse é aquele outro tipo de prazer mais elevado e mais sublime que o anterior</span>.[<a
href="#N3">3</a>]</p>



<p class=Normal><a name=R3></a>11. “<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Se alguém dissesse ... E qual é esse outro tipo de prazer? Aqui, Ananda,
com a completa superação das percepções da forma, com o desaparecimento das
percepções do contato sensorial, sem dar atenção às percepções da diversidade,
consciente de que o ‘espaço é infinito,’ um bhikkhu entra e permanece na base
do espaço infinito. Esse é aquele outro tipo de prazer mais elevado e mais
sublime que o anterior</span>.</p>



<p class=Normal>12. “<span lang=PT-BR style='mso-ansi-language:PT-BR'>Se
alguém dissesse ... E qual é esse outro tipo de prazer? Aqui, Ananda, com a
completa superação da base do espaço infinito, consciente de que a ‘consciência
é infinita,’ um bhikkhu entra e permanece na base da consciência infinita. Esse
é aquele outro tipo de prazer mais elevado e mais sublime que o anterior</span>.</p>



<p class=Normal>13. “<span lang=PT-BR style='mso-ansi-language:PT-BR'>Se
alguém dissesse ... E qual é esse outro tipo de prazer? Aqui, Ananda,<span
style="mso-spacerun: yes">  </span>com a completa superação da base da
consciência infinita, consciente de que ‘não há nada, ‘ um bhikkhu entra e
permanece na base do nada. Esse é aquele outro tipo de prazer mais elevado e
mais sublime que o anterior</span>.</p>


<p class=Normal>14. “<span lang=PT-BR style='mso-ansi-language:PT-BR'>Se
alguém dissesse ... E qual é esse outro tipo de prazer? Aqui, Ananda, com a
completa superação da base do nada, um bhikkhu entra e permanece na base da nem
percepção, nem não percepção. Esse é aquele outro tipo de prazer mais elevado e
mais sublime que o anterior</span>.</p>



<p class=Normal>15. “<span lang=PT-BR style='mso-ansi-language:PT-BR'>Se
alguém dissesse: “Esse é o máximo em prazer e alegria que os seres
experimentam,’ eu não admitiria isso. Por que não? Porque existe um outro tipo
de prazer mais elevado e mais sublime do que aquele prazer. E qual é esse outro
tipo de prazer? Aqui, Ananda, com a completa superação da base da nem
percepção, nem não percepção, um bhikkhu entra e permanece na cessação da
percepção e sensação. Esse é aquele outro tipo de prazer mais elevado e mais
sublime que o anterior</span>.</p>



<p class=Normal>16. “<span lang=PT-BR style='mso-ansi-language:PT-BR'>É
possível, Ananda, que os errantes de outras seitas possam dizer isto: ‘O
contemplativo Gotama fala da cessação da percepção e sensação e descreve isso
como prazer. O que é isso e como é isso?’ Aos errantes de outras seitas que
falam isso deveria ser dito: ‘Amigos. O Abençoado descreve o prazer não se
referindo só à sensação prazerosa, mais do que isso, amigos, o Tathagata
descreve como prazer todo tipo de prazer onde quer e em qualquer forma que ele seja encontrado</span>.” [<a href="#N4">4</a>]</p>


<p class=Normal><a name=R31></a><a name=R4></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>Isso foi o que disse o Abençoado. O </span>venerável
Ananda <span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style="mso-spacerun: yes"> </span>ficou satisfeito e contente com as palavras
do Abençoado. <o:p></o:p></span></p>


