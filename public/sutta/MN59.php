<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[2] Os </span><i>dois tipos</i> de sensações são sensações no corpo e
sensações na mente, ou, (de forma menos comum) os dois tipos mencionados por
Pancakanga no verso 3. Os <i>três tipos </i>são os três mencionados por Udayin
no verso 3. Os <i>cinco tipos </i>são as faculdades do prazer (corporal),
alegria (mental), dor (corporal), tristeza (mental) e equanimidade. Os <i>seis
tipos </i>são as sensações que nascem à partir das seis faculdades dos
sentidos. Os <i>dezoito tipos </i>são os dezoito tipos de examinação mental –
investigando os seis objetos dos sentidos que podem produzir a alegria,
produzir a tristeza e produzir a equanimidade (veja <a href="MN137.php#T8">MN
137.8</a>). Os <i>trinta e seis tipos</i> são os trinta e seis estados (ou
posições) dos seres – os seis tipos de alegria, tristeza e equanimidade cada um
baseado ou na vida em família ou na renúncia (veja <a href="MN137.php#T9">MN
137.9-15</a>). Os <i>cento e oito tipos</i> são os trinta e seis mencionados a
pouco considerados em referência ao passado, presente e futuro. Veja também o <a
href="SNXXXVI.22.php">SN XXXVI.22</a> [<a href="#R2">Retorna</a>]<span
lang=PT-BR style='mso-ansi-language:PT-BR'><o:p></o:p></span></p>


