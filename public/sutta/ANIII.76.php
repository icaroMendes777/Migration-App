<p class=Normal><a name=N2></a> [2] Plano inferior é o reino da esfera sensual, plano médio é o reino da matéria sutil e plano superior é o reino imaterial. O caminho do Buda visa superar o renascimento em todos os reinos. [<a href="#R2">Retorna</a>]</p> 

