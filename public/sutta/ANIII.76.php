<p class=Normal> Então o venerável Ananda foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse: </p>

<p class=Normal>"Venerável senhor, é dito: 'ser/existir, ser/existir.' De que modo, venerável senhor, há o ser/existir?" [<a href="#N1">1</a>] </p>

<p class=Normal><a name=R1></a> (1) Ananda, se não houvesse o amadurecimento de kamma no reino da esfera sensual, o ser/existir no reino da esfera sensual seria discernido?" </p>

<p class=Normal>"Não, venerável senhor." </p>

<p class=Normal>"Ananda, portanto, para os seres atrapalhados pela ignorância e agrilhoados pelo desejo, kamma é o campo, a consciência a semente, e o desejo a umidade para que a consciência se estabeleça no plano inferior. [<a href="#N2">2</a>]<a name=R2></a> É dessa forma que há a produção de um renovado ser/existir no futuro. </p>

<p class=Normal> (2) Ananda, se não houvesse o amadurecimento de kamma no reino da matéria sutil, o ser/existir no reino da matéria sutil seria discernido?" </p>

<p class=Normal>"Não, venerável senhor." </p>

<p class=Normal>"Ananda, portanto, para os seres atrapalhados pela ignorância e agrilhoados pelo desejo, kamma é o campo, a consciência a semente, e o desejo a umidade para que a consciência se estabeleça no plano médio. É dessa forma que há a produção de um renovado ser/existir no futuro. </p>

<p class=Normal> (3) Ananda, se não houvesse o amadurecimento de kamma no reino imaterial, o ser/existir no reino imaterial seria discernido?" </p>

<p class=Normal>"Não, venerável senhor." </p>

<p class=Normal>"Ananda, portanto, para os seres atrapalhados pela ignorância e agrilhoados pelo desejo, kamma é o campo, a consciência a semente, e o desejo a umidade para que a consciência se estabeleça no plano superior. É dessa forma que há a produção de um renovado ser/existir no futuro. </p>

<p class=Normal>"É desse modo, Ananda, que há o ser/existir." </p>


<p class=Normal>O AN III.77 é idêntico a este sutta exceto pela seguinte modificação: </p>

<p class=Normal> (76) " ... para os seres atrapalhados pela ignorância e agrilhoados pelo desejo, kamma é o campo, a consciência a semente e o desejo a umidade para que a <i>consciência</i> se estabeleça ... "</p>

<p class=Normal> (77) " ... para os seres atrapalhados pela ignorância e agrilhoados pelo desejo, kamma é o campo, a consciência a semente e o desejo a umidade para que a <i>volição e aspiração</i> se estabeleça ... " </p>

