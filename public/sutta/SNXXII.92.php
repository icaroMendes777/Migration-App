<p class=Normal> Em Savatthi. Então o Venerável Rahula se aproximou do Abençoado e depois de cumprimentá-lo sentou a um lado e disse: </p>

<p class=Normal> “Venerável senhor, como alguém pode saber, como alguém pode ver, para que em relação a este corpo com a sua consciência e todos os sinais externos, não exista a fabricação de um eu, a fabricação de um meu e a tendência subjacente à presunção, ele tenha transcendido a discriminação, e esteja em paz e bem libertado?” </p>

<p class=Normal> “Qualquer tipo de forma material, Rahula, quer seja do passado, do futuro ou do presente, interna ou externa, grosseira ou sutil, inferior ou superior, próxima ou distante – a pessoa vê toda forma material como na verdade ela é, com correta sabedoria, desta forma: ‘Isso não é meu, isso não sou eu, isso não é o meu eu’”. </p>

<p class=Normal> “Qualquer tipo de sensação ... Qualquer tipo de percepção ... Qualquer tipo de formações volitivas ... Qualquer tipo de consciência ... a pessoa vê toda consciência tal qual na verdade ela é, com correta sabedoria, desta forma: ‘Isso não é meu, isso não sou eu, isso não é o meu eu’”. </p>

<p class=Normal>“É quando alguém sabe e vê dessa forma, Rahula, que em relação a este corpo com a sua consciência e todos os sinais externos, não existe a fabricação de um eu, a fabricação de um meu e a tendência subjacente à presunção, ele transcendeu a discriminação e está em paz e bem libertado.” </p>


