<p class=Normal> Em certa ocasião, o Abençoado estava em Rajagaha, no Bambual, no Santuário dos Esquilos.</p>

<p class=Normal> Naquela ocasião havia duas facções enamoradas e apaixonadas por uma certa cortesã. Eles estavam envolvidos em brigas, rixas, e disputas, atacando uns aos outros com os punhos, com torrões, com paus, e com facas, causando a si próprios a morte ou sofrimento igual à morte.</p>

<p class=Normal> Então, ao amanhecer, um número de bhikkhus se vestiram e tomando a tigela e o manto externo, foram para Rajagaha para esmolar alimentos. Depois de haver esmolado alimentos e haver retornado, após a refeição, eles foram até o Abençoado e depois de cumprimentá-lo sentaram a um lado e disseram: "Venerável senhor, no momento há duas facções ... morte ou sofrimento igual à morte."</p>

<p class=Normal>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p>

<p class=Normal style='margin-left:.75in'> O que foi realizado, o que falta ser realizado,
ambos estão contaminados<br>
no caso daquele que treina em conformidade com a aflição. <br>
Aqueles que consideram como essência o treinamento, ou os preceitos, ou o modo de vida, ou o celibato, ou o servir - esse é um extremo. <br>
Qualquer um que diga, 'Não há perigo nos desejos pelos prazeres dos sentidos': esse é o outro extremo. <br>
Ambos extremos incham os cemitérios, e os cemitérios fazem crescer o entendimento incorreto. Sem o conhecimento direto desses dois extremos alguns aderem e outros se excedem. [<a href="#N1">1</a>]<br>Mas aqueles com o conhecimento direto, sem estar naquilo, sem conceber com base naquilo. [<a href="#N2">2</a>] Não existe ciclo no qual eles se manifestam. </p>

