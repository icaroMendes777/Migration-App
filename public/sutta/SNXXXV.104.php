<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, eu farei para vocês uma exposição do Dhamma sobre aquele
que declara o esforço para se tornar seguro contra o cativeiro. [<a href="#N1">1</a>]
<a name=R1></a>Ouçam e prestem muita atenção àquilo que eu vou dizer.” – “Sim,
venerável senhor,” os bhikkhus responderam. O Abençoado disse o seguinte: <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é a exposição do Dhamma sobre aquele que declara o esforço para se
tornar seguro contra o cativeiro? Existem, bhikkhus, formas conscientizadas através
do olho que são desejáveis, agradáveis e fáceis de serem gostadas, conectadas
com o desejo sensual e que provocam a cobiça. Elas foram abandonadas pelo Tathagata, cortadas pela raiz, feitas como com um tronco de palmeira,
eliminando-as de tal forma que não estarão mais sujeitas a um futuro
surgimento. Ele declara que o esforço [deve ser feito] para o seu abandono.
Portanto, o Tathagata é chamado aquele que declara o esforço para se tornar
seguro contra o cativeiro. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Existem,
bhikkhus, sons conscientizados através do ouvido ... objetos mentais conscientizados pela
mente que são desejáveis, agradáveis e fáceis de serem gostados, conectados com
o desejo sensual e que provocam a cobiça. Eles foram abandonados pelo
Tathagata, cortados pela raiz, como um tronco de palmeira, eliminando-os de tal
forma que não estarão mais sujeitos a um futuro surgimento. Ele declara que o
esforço [deve ser feito] para o seu abandono. Portanto, o Tathagata é chamado
aquele que declara o esforço para se tornar seguro contra o cativeiro.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Essa,
bhikkhus, é a exposição do Dhamma sobre aquele que declara o esforço para se
tornar seguro contra o cativeiro.”<o:p></o:p></span></p>

