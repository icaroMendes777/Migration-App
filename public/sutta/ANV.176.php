<p class=Normal> Então o chefe de família Anathapindika, acompanhado de quinhentos discípulos leigos, foi até o Abençoado e depois de cumprimentá-lo sentou a um lado. O Abençoado então disse: </p>

<p class=Normal> <a name=R1></a>"Chefes de Família, vocês presentearam a Sangha dos bhikkhus com mantos, comida, moradias e medicamentos. Vocês não deveriam estar satisfeitos apenas com isso, pensando: 'Nós presenteamos a Sangha dos bhikkhus com mantos, comida, moradias e medicamentos.' Portanto, chefes de família, vocês deveriam treinar assim: 'Como podemos de tempos em tempos entrar e permanecer no êxtase do isolamento?' [<a href="#N1">1</a>] É assim que vocês deveriam treinar." </p>

<p class=Normal> Quando isso foi dito, o venerável Sariputta disse para o Abençoado: "É maravilhoso e admirável, venerável senhor, quão bem isso foi dito pelo Abençoado. Venerável senhor, sempre que um nobre discípulo entra e permanece no êxtase do isolamento, nessa ocasião cinco coisas não lhe ocorrem. (1) Sofrimento e tristeza conectados com o desejo sensual  não lhe ocorrem. (2) Prazer e alegria conectados com o desejo sensual  não lhe ocorrem. (3) Sofrimento e tristeza conectados com o prejudicial não lhe ocorrem. (4) Prazer e alegria conectados com o prejudicial não lhe ocorrem. (5) Sofrimento e tristeza conectados com o benéfico não lhe ocorrem. Venerável senhor, sempre que um nobre discípulo entra e permanece no êxtase do isolamento, nessa ocasião essas cinco coisas não lhe ocorrem." </p>

<p class=Normal> "Muito bem, Sariputta! Sariputta, sempre que um nobre discípulo entra e permanece no êxtase do isolamento, nessa ocasião cinco coisas não lhe ocorrem. (1) Sofrimento e tristeza conectados com o desejo sensual  não lhe ocorrem. (2) Prazer e alegria conectados com o desejo sensual  não lhe ocorrem. (3) Sofrimento e tristeza conectados com o prejudicial não lhe ocorrem. (4) Prazer e alegria conectados com o prejudicial não lhe ocorrem. (5) Sofrimento e tristeza conectados com o benéfico não lhe ocorrem. Sempre que um nobre discípulo entra e permanece no êxtase do isolamento, nessa ocasião essas cinco coisas não lhe ocorrem." </p>


