corpo, após a morte, renasce na companhia dos supannas que nascem de ovos?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
bhikkhu, alguém age de forma ambivalente com o corpo, linguagem e mente. Ele
ouviu: ‘Os supannas nascidos de ovos vivem por muito tempo, são belos e abundam
em felicidade.’ Ele pensa: ‘Oh, que com a dissolução do corpo, após a morte, eu
possa renascer na companhia dos supannas que nascem dos ovos!’ Então, com a
dissolução do corpo, após a morte, ele renasce na companhia dos supannas que
nascem dos ovos.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Essa,
