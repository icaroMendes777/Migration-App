<p class=Normal> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: "Um bhikkhu que não é ardente e sem o temor de cometer transgressões é incapaz de realizar a iluminação, incapaz de realizar nibbana, incapaz de alcançar a suprema segurança contra o cativeiro. Mas um bhikkhu que é ardente e teme cometer transgressões é capaz de realizar a iluminação, capaz de realizar nibbana, capaz de alcançar a suprema segurança contra o cativeiro."</p>

<p class=Normal style='margin-left:.75in'> Quem não é ardente, não teme cometer transgressões, <br>
preguiçoso, com pouca energia, <br>
cheio de preguiça e torpor, <br>
sem vergonha de cometer transgressões e sem respeito -<br>
um bhikkhu assim é incapaz<br>
de realizar a iluminação suprema.</p>

<p class=Normal style='margin-left:.75in'>Mas um meditador com  atenção plena, <br>
ardente, temendo cometer transgressões, diligente, <br>
tendo rompido o grilhão do nascimento e envelhecimento, <br>
pode realizar aqui e agora<br>
a iluminação suprema.</p>


