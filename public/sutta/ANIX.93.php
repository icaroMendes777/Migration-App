<p class=Normal> (93) "Bhikkhus, para o conhecimento direto da cobiça, nove coisas devem ser desenvolvidas. Quais nove? A percepção do repulsivo, a percepção da morte, a percepção do repulsivo na comida, a percepção de não-deleite com tudo no mundo, a percepção da impermanência, a percepção do sofrimento na impermanência, a percepção de não-eu naquilo que é sofrimento, a percepção do abandono, e a percepção do desapego. Para o conhecimento direto da cobiça, essas nove coisas devem ser desenvolvidas." </p>

<p class=Normal> (94) "Bhikkhus, para o conhecimento direto da cobiça, nove coisas devem ser desenvolvidas. Quais nove? O primeiro jhana, o segundo jhana, o terceiro jhana, o quarto jhana, a base do espaço infinito, a base da consciência infinita, a base do nada, a base da nem percepção, nem não percepção, e a cessação da percepção e sensação. Para o conhecimento direto da cobiça, essas nove coisas devem ser desenvolvidas." </p>

<p class=Normal> (95-112) "Bhikkhus, para a completa compreensão da cobiça ... para a completa destruição ... para o abandono ... para a destruição ... para o desaparecimento ... para o desapego ... para a cessação .... para abrir mão ... para a renúncia da cobiça, essas nove coisas devem ser desenvolvidas." </p>

<p class=Normal> (113-432) "Bhikkhus, para o conhecimento direto ... para a completa compreensão .... para a completa destruição ... para o abandono ... para a destruição ... para o desaparecimento ... para o desapego ... para a cessação .... para abrir mão ... para a renúncia da raiva ... da delusão ... do ódio ... da hostilidade ... da difamação ... da insolência ... da inveja ... da avareza ... da enganação ... da astúcia ... da teimosia .... da veemência ... da presunção ... da arrogância ... da embriaguez .... da negligência, essas nove coisas devem ser desenvolvidas." </p>


