<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então,
quando o meio mês havia passado, o Abençoado saiu do isolamento e se dirigiu
aos bhikkhus da seguinte forma:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
nesse meio mês eu permaneci no mesmo estado em que permaneci logo depois de ter
alcançado a perfeita iluminação. Eu compreendi desta forma: ‘Há sensações que
têm o entendimento incorreto como condição e também sensações que têm o
entendimento correto como condição. Há sensações que têm a concentração
incorreta como condição e também há sensações que têm a concentração correta
como condição. Há sensações que têm o desejo como condição e também há
sensações que têm o pensamento como condição e também há sensações que têm a
percepção como condição. [<a href="#N2">2</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Quando o desejo não decresceu e o pensamento não decresceu e a percepção
não decresceu, há a sensação tendo isso como condição. Quando o desejo
decresceu, e os pensamentos não decresceram, e as percepções não decresceram,
também há a sensação tendo isso como condição. Quando o desejo decresceu, e os
pensamentos decresceram, e as percepções não decresceram, também há a sensação
tendo isso como condição. Quando o desejo decresceu, e os pensamentos
decresceram, e as percepções decresceram, também há a sensação tendo isso como
condição. Há o esforço para realizar o ainda não realizado. Quando esse estágio
tiver sido alcançado, também há sensação tendo isso como condição.”<a name=R3></a>
