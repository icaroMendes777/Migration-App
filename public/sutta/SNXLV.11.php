<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Notas:<o:p></o:p></span></p>



<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] Porque o Buda diz isso? De acordo com o comentário, naquela quinzena
o Buda não tinha ninguém a quem instruir, então, ele pensou: “Permanecerei esta
quinzena na bem-aventurança da fruição do estado de arahant. Assim, terei uma
permanência prazerosa e servirei como exemplo para as gerações futuras.” [<a
href="#R1">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[2] Sensações que têm o desejo, <i>(chanda), </i>como condição são as
sensações associadas com os oito tipos de consciência acompanhadas pela cobiça;
aquelas condicionadas pelo pensamento são as sensações do primeiro jhana; e
aquelas condicionadas pela percepção são as sensações das seis realizações meditativas
a partir do segundo jhana até a base do nada. [<a href="#R2">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[3] O comentário explica que a sensação que ocorre quando nenhum dos
três decresceram é a sensação associada com os oito tipos de consciência acompanhadas
pela cobiça. A sensação quando só o desejo decresceu, é aquela do primeiro
jhana; a sensação quando apenas a percepção permanece, é aquela do segundo
jhana e dos jhanas superiores. A sensação quando todos os três decresceram, é
aquela da base da nem percepção, nem não percepção. O “ainda não realizado” é o
fruto do estado de arahant. A última expressão se refere à sensação que
acompanha os quatro caminhos supramundanos. A palavra “esforço” neste caso é “<i>ayama</i>”,
interpretada pelo comentário como <i>viriya</i>, energia.
