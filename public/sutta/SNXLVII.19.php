<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em certa
ocasião, o Abençoado estava entre os Sumbhas em uma cidade denominada Sedaka. Lá o Abençoado se dirigiu aos bhikkhus desta forma: <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus, certa vez no
passado, um acrobata, tendo levantado um poste de bambu, se dirigiu ao seu
ajudante, Medakathalika: ‘Venha, estimado Medakathalika, suba nos meus ombros e
fique em pé sobre o poste de bambu.’ Tendo respondido, ‘Sim, mestre,’ o
ajudante Medakathalika subiu sobre os ombros dele e ficou em pé no poste de
bambu. O acrobata então disse para o ajudante Medakathalika: ‘Você agora me
proteja, estimado Medakathalika, que eu o protegerei. Assim, guardando um ao
outro, protegendo um ao outro, mostraremos nossa arte, receberemos nossa recompensa
e com segurança desceremos do poste de bambu.’ Quando isso foi dito o ajudante
Medakathalika respondeu: ‘Esse não é o modo de fazer isso, mestre. Você se proteje,
mestre, que eu me protegerei. Assim, com cada um guardado e protegido, nós
mostraremos a nossa arte, receberemos a nossa recompensa e com segurança
desceremos do poste de bambu.’ <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Esse é o método nesse
caso,” o Abençoado disse. “É exatamente aquilo que o ajudante Medakathalika
disse para o seu mestre. ‘Eu me protegerei,’ bhikkhus: assim devem ser
praticados os fundamentos da atenção plena. ‘Eu protegerei os outros,’
bhikkhus: assim devem ser praticados os fundamentos da atenção plena.
Protegendo a si mesmo, bhikkhus, ele protege os outros; protegendo os outros,
ele protege a si mesmo.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como é, bhikkhus, que
protegendo a si mesmo, ele protege os outros? Através da perseverança,
desenvolvimento e cultivo dos quatro fundamentos da atenção plena. É desse modo
que protegendo a si mesmo, ele protege os outros.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como é, bhikkhus, que
protegendo os outros, ele protege a si mesmo? Através da paciência, não fazendo
o mal, através de uma mente com amor bondade e compaixão. É desse modo que
protegendo os outros, ele protege a si mesmo. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“‘Eu protegerei a mim mesmo,’
bhikkhus: assim devem ser praticados os fundamentos da atenção plena. ‘Eu
protegerei os outros,’ bhikkhus: assim devem ser praticados os fundamentos da
atenção plena. Protegendo a si mesmo, bhikkhus, ele protege os outros;
protegendo os outros, ele protege a si mesmo.”<span style='mso-spacerun:yes'>  
