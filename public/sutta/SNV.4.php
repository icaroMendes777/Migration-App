<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. Então, ao amanhecer, a bhikkhuni Vijaya se vestiu e tomando a tigela
e o manto externo foi para Savatthi esmolar alimentos. Depois de haver esmolado
alimentos em Savatthi e de haver retornado, após a refeição, ela se dirigiu ao
bosque dos Homens Cegos em busca de isolamento. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então Mara, o Senhor do
Mal, desejando despertar medo, trepidação e terror na bhikkhuni Vijaya, desejando
que ela abandonasse a sua concentração, se aproximou e se dirigiu a ela em
versos:<span style="mso-spacerun: yes">  </span><o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
 “Você é tão jovem e bela,<br>
e eu também estou na flor da juventude.<br>
Venha, nobre senhora, vamos nos regozijar<br>
com a música de um quinteto.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Foi então que a bhikkhuni
Vijaya pensou: “Agora, quem será que recitou esses versos – um ser humano ou um
ser não humano?” Então lhe ocorreu: “Foi Mara, o Senhor do Mal, quem recitou
esses versos desejando despertar medo, trepidação e terror em mim, desejando
que eu abandone a minha concentração.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, a bhikkhuni Vijaya,
tendo compreendido, “Este é Mara, o Senhor do Mal,” respondeu em versos: <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
 “Formas e sons, sabores e aromas,<br>
tangíveis que deliciam a mente:<br>
eu os devolvo imediatamente para você, <br>
pois eu, Oh Mara, não preciso disso. <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
Eu sinto repulsa e
humilhação<br>
por este corpo imundo e pútrido,<br>
sujeito à dissolução, frágil:<br>
eu desenraizei o desejo sensual. <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
Quanto aos seres que
transitam pela forma, <br>
e aqueles que compartem do sem forma,<br>
e aquelas realizações pacíficas também:<br>
por toda parte a escuridão foi destruída.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, Mara, o Senhor do
Mal, compreendendo que, “A bhikkhuni Vijaya sabe quem sou,” triste e desapontado,
desapareceu no mesmo instante. <o:p></o:p></span></p>



