<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><i>Venerável
Punnaka:</i> “Para aquele que está livre do desejo, que viu a raiz (das coisas), [<a
href="#N1">1</a>] <a name=R1></a>eu tenho uma pergunta: qual é a razão porque
aqui neste mundo sábios, governantes, brâmanes e outros homens fazem vários
tipos de oferendas para os devas? Eu lhe pergunto isso, Abençoado, por favor
diga-me a resposta.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><i><i>Buda:</i></i>
“Todos sábios, governantes, brâmanes e outros homens, Punnaka, que fazem vários
tipos de oferendas para os devas, assim o fazem na esperança desta ou daquela
vida futura, sendo induzidos pelo (fato do) envelhecimento.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Punnaka:
“Ao fazerem vários tipos de oferendas para os devas, sendo diligentes nas oferendas,
eles superam o nascimento e o envelhecimento, venerável senhor?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><i><i>Buda:</i></i> “As
suas orações, oferendas, elogios e aspirações, são todas feitas com base na
posse, na recompensa: eles desejam os prazeres dos sentidos. Esses devotos das
oferendas, encantados pela cobiça pela existência, [<a href="#N2">2</a>] <a
name=R2></a>não superam o nascimento e envelhecimento, eu digo.”<span
style='mso-spacerun:yes'>  </span><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Punnaka:
“Se esses devotos dos sacrifícios não superam o nascimento e o envelhecimento através
das oferendas, venerável senhor, então através de qual prática alguém supera o
nascimento e o envelhecimento neste mundo de devas e humanos?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><i><i>Buda:</i></i>
“Aquele que compreendeu no mundo o aqui e o além, em quem não há perturbação
por nada no mundo, que está tranqüilo, livre das chamas ardentes, [<a href="#N3">3</a>]
<a name=R3></a>despreocupado e livre do desejo – ele superou o nascimento e o
envelhecimento, eu digo.” <o:p></o:p></span></p>

