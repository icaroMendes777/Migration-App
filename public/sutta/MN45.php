<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. <a name=R1></a>Em certa ocasião o Abençoado estava em Savatthi no Bosque
de Jeta, no Parque de Anathapindika. Lá ele se dirigiu aos monges desta forma:
“Bhikkhus.” – “Venerável Senhor,” eles responderam. O Abençoado disse o
seguinte:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.
“Bhikkhus, existem quatro maneiras de fazer as coisas. Quais quatro? Existe uma
maneira de fazer as coisas que é prazerosa agora e amadurece no futuro como
dolorosa. Existe uma maneira de fazer as coisas que é dolorosa agora e
amadurece no futuro como dolorosa. Existe uma maneira de fazer as coisas que é
dolorosa agora e amadurece no futuro como prazerosa. Existe uma maneira de
fazer as coisas que é prazerosa agora e amadurece no futuro como prazerosa.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. “Como,
bhikkhus, é a maneira de fazer as coisas que é prazerosa agora e amadurece no
futuro como dolorosa? Bhikkhus, existem certos contemplativos e brâmanes cuja
doutrina e entendimento é este: ‘Não existe perigo no prazer sensual.’ Eles são
apegados à devoração de prazeres sensuais e se divertem com mulheres errantes
que usam o cabelo preso num coque acima da cabeça. Eles assim dizem: ‘Qual medo
futuro esses bons contemplativos e brâmanes vêm nos prazeres sensuais quando
eles falam no seu abandono e descrevem a completa compreensão dos prazeres
sensuais? Prazeroso é o toque delicado, suave, fofo, do braço dessa mulher!’
Assim eles são apegados à devoração de prazeres sensuais, e tendo feito isso,
na dissolução do corpo, depois da morte, eles renascem num estado de privação,
num destino infeliz, nos reinos inferiores, até mesmo no <span
style='mso-bidi-font-weight:bold'>inferno</span>. Lá eles sentem sensações
dolorosas, torturantes, penetrantes. Eles dizem o seguinte: ‘Esse é o medo
futuro que aqueles bons contemplativos e brâmanes viram nos prazeres sensuais
ao falarem sobre o seu abandono e descreverem a completa compreensão dos prazeres
sensuais. Pois é por causa dos prazeres sensuais, devido aos prazeres sensuais,
que agora estamos sentindo sensações dolorosas, torturantes, penetrantes.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4.
“Bhikkhus, suponham que no último mês da estação quente o legume de uma
trepadeira arrebentasse e uma semente de trepadeira caísse ao pé de uma árvore
sal. Então um deva que vive naquela árvore ficaria medroso, perturbado e
assustado; mas os amigos e companheiros, pares e parentes do deva – devas dos
jardins, devas dos parques, devas das árvores e os devas que habitam as ervas
medicinais, o capim e as árvores monarca – se reuniriam e acalmariam o deva
assim: ‘Não tenha medo, senhor, não tenha medo. Quem sabe um pavão engolirá a
semente da trepadeira ou um animal selvagem irá comê-la, ou um fogo na floresta
irá queim­á-la, ou matutos irão levá-la, ou formigas brancas irão devorá-la, ou
ela poderá nem mesmo ser fértil.’ Mas nenhum pavão engoliu aquela semente,
nenhum animal selvagem a comeu, nenhum fogo na floresta a queimou, nenhum
matuto a levou, nenhuma formiga branca a devorou e na verdade ela era fértil.
Então, sendo umedecida pela água de uma nuvem carregada de chuva, a semente no
seu devido tempo germinou e o rebento delicado, suave, fofo se enroscou em
volta daquela árvore sal. Então o deva que vivia na árvore sal pensou:<span
style="mso-spacerun: yes">  </span>‘Qual medo futuro viram os meus amigos e
companheiros, pares e parentes ... naquela semente de trepadeira quando eles se
reuniram e me acalmaram da forma como fizeram? Prazeroso é o toque delicado,
suave, fofo desse rebento da trepadeira!’ Então a trepadeira envolveu a árvore
sal, fez uma abóbada sobre ela, estendeu uma cortina em toda sua volta e rachou
os principais galhos da árvore. O deva que vivia na árvore então compreendeu:
‘Esse é o medo temor que eles viram naquela semente de trepadeira. Por causa
daquela semente de trepadeira eu agora estou sentindo sensações dolorosas,
torturantes, penetrantes.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Da mesma
forma, bhikkhus, existem certos contemplativos e brâmanes cuja doutrina e
entendimento é este: ‘Não existe perigo no prazer sensual.’... Eles dizem o
seguinte: ‘Esse é o medo futuro que aqueles bons contemplativos e brâmanes
viram nos prazeres sensuais ... que agora estamos sentindo sensações dolorosas,
torturantes, penetrantes.’ Essa é chamada a maneira de fazer as coisas que é
prazerosa agora e que amadurece no futuro como dolorosa.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. “E como,
bhikkhus, é a maneira de fazer as coisas que é dolorosa agora e amadurece no
futuro como dolorosa? Neste caso, bhikkhus, alguém anda nu, rejeitando as
convenções, lambendo as mãos, não atendendo quando chamado, não parando quando
solicitado ... (igual ao </span><a href="MN12.php#T45"><span lang=PT-BR
style='mso-ansi-language:PT-BR'>MN 12, verso 45</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>) ... Ele purifica o corpo com três imersões na
água a cada dia. Assim, de variadas formas ele permanece se dedicando à prática
de atormentar e mortificar o corpo. Na dissolução do corpo, depois da morte,
ele renasce num estado de privação, num destino infeliz, nos reinos inferiores,
até mesmo no <span style='mso-bidi-font-weight:bold'>inferno</span>. Essa é
chamada a maneira de fazer as coisas que é dolorosa agora e amadurece no futuro
como dolorosa.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. “E como,
bhikkhus, é a maneira de fazer as coisas que é dolorosa agora e amadurece no
futuro como prazerosa? Neste caso, bhikkhus, alguém normalmente tem intensa cobiça 
por natureza, e ele experimenta com freqüência a dor e a angústia nascidas da cobiça; 
alguém normalmente tem intensa raiva por natureza, e ele experimenta com freqüência 
a dor e a angústia nascidas da raiva; alguém normalmente tem intensa delusão por natureza, 
e ele experimenta com freqüência a dor e a angústia nascidas da delusão. Apesar
disso, com dores e angústias, em prantos com o rosto coberto de lágrimas, ele
vive a vida santa perfeita e pura. Na dissolução do corpo, após a morte, ele
renasce num destino feliz, até mesmo no paraíso. Essa é chamada a maneira de
fazer as coisas que é dolorosa agora e amadurece no futuro como prazerosa.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. “E como,
bhikkhus, é a maneira de fazer as coisas que é prazerosa agora e amadurece no
futuro como prazerosa? Neste caso, bhikkhus, alguém normalmente não tem intensa cobiça 
por natureza, e ele não experimenta com freqüência a dor e a angústia nascidas da cobiça; 
alguém normalmente não tem intensa raiva por natureza, e ele não experimenta com freqüência 
a dor e a angústia nascidas da raiva; alguém normalmente não tem intensa delusão por natureza, 
e ele não experimenta com freqüência a dor e a angústia nascidas da delusão. Afastado dos prazeres sensuais, afastado das
qualidades não hábeis, ele entra e permanece no primeiro jhana ... abandonando o
pensamento aplicado e sustentado, ele entra e permanece no segundo jhana ...
Abandonando o êxtase ... ele entra e permanece no terceiro jhana ... Com completo desaparecimento da felicidade ... 
ele entra e permanece no quarto jhana
... Na dissolução do corpo, após a morte, ele renasce num destino feliz, até mesmo
no paraíso. Essa é chamada a maneira de fazer as coisas que é prazerosa agora e
amadurece no futuro como prazerosa. Essas, bhikkhus, são as quatro maneiras de
fazer as coisas.”<o:p></o:p></span></p>



<p class=Normal><a name=R31></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Isso foi o que disse o Abençoado. Os bhikkhus ficaram satisfeitos e
contentes com as palavras do Abençoado. <o:p></o:p></span></p>


