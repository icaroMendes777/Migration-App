<p class=Normal>
Ouvi que em certa ocasião o Abençoado estava em Kimila, no bambual.
Então o Venerável Kimila  foi
até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse:  “Qual é a causa
Senhor, qual é a razão porque quando um Tathagata realiza o parinibbana, o
verdadeiro Dhamma não sobrevive por um longo tempo?”<o:p></o:p></span></p>


<p class=Normal>
“Kimila, há a situação em que, quando um Tathagata realiza o parinibbana,
os Bhikkhus, Bhikkhunis, discípulos leigos e discípulas leigas vivem sem
respeito, sem consideração pelo Mestre; vivem sem respeito, sem consideração
pelo Dhamma ... pela Sangha ... pelo Treinamento .... pela concentração ...
pela diligência; vivem sem respeito, sem consideração pela hospitalidade. Essa é a
causa, essa é a razão porque quando um Tathagata realiza o parinibbana o
verdadeiro Dhamma não sobrevive por um longo tempo.”<o:p></o:p></span></p>


<p class=Normal>
“E qual é a causa, qual é a razão porque quando um Tathagata realiza o parinibbana
o verdadeiro Dhamma<span style='mso-spacerun:yes'>  </span>sobrevive por um
longo tempo?”<o:p></o:p></span></p>


