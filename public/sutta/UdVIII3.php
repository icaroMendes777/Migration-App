a emancipação do nascido - do que é - do fabricado - do condicionado seria
discernida. Porém precisamente porque há o que não nasceu - o que não é - o que
não é fabricado - o que não é condicionado, a emancipação do nascido - do que é
- do fabricado - do condicionado é discernida.&quot; <o:p></o:p></span></p>


