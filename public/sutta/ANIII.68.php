<p class=Normal><b>Notas:</b></p>


<p class=Normal><a name="N1"> </a> [1] Paixão, (<i>raga</i>), é um sinônimo de cobiça, (<i>lobha</i>). Cobiça é o termo que aparece com mais freqüência quando são mencionadas as três raizes de ações prejudiciais ou inábeis, (<i>akusala-mula</i>). [<a href="#R1">Retorna</a>] </p>
 
<p class=Normal><a name="N2"> </a> [2] Para demonstrar que a paixão é menos censurável, AA oferece como exemplo o fato que não há nenhum estigma social associado ao casamento embora este esteja fundamentado no desejo sexual e, neste caso, se a paixão estiver limitada ao que prescrevem os padrões morais da sociedade, esse tipo de paixão, por si só, não irá conduzir a um renascimento desfavorável. Por conseguinte ela é menos censurável sob o ponto de vista da sociedade bem como das conseqüências de kamma. Mas como o desejo sexual tem raizes profundas na natureza humana, ele “é difícil de ser removido tal como a fuligem oleosa, e é o tipo de apego que pode ter seguimento por algumas vidas.” </p>

<p class=Normal>A raiva e a delusão são censuráveis sob o ponto vista da sociedade e trazem conseqüências de kamma ruins, porque ambas podem conduzir a um renascimento num dos planos inferiores. A raiva no entanto, é um estado mental desagradável, e visto que os seres buscam a felicidade, em geral eles tentarão se livrar da raiva. Idéias deludidas no entanto, se estiverem profundamente enraizadas, serão tão difíceis de remover como o desejo sexual. [<a href="#R2">Retorna</a>] </p>

