<p class=Normal> “Bhikkhus, se vocês forem perguntados pelos errantes de outras seitas, ‘Amigos, há essas três qualidades. Quais três? Paixão, raiva e delusão. [<a href="#N1">1</a>] <a name="R1"> </a>Essas são as três qualidades. Agora qual é a diferença, qual é a distinção, qual é o fator que distingue essas três qualidades?’ – ao serem assim perguntados, como vocês responderiam aos errantes de outras seitas?” </p>

<p class=Normal> “Venerável senhor, os nossos ensinamentos têm o Abençoado como origem, como guia e como refúgio. Seria bom se o Abençoado pudesse explicar o significado dessas palavras. Tendo ouvido do Abençoado, os bhikkhus o recordarão.” </p>

<p class=Normal> “Então, ouçam e prestem muita atenção àquilo que eu vou dizer.” – “Sim, venerável senhor,” os bhikkhus responderam. O Abençoado disse o seguinte: </p>

<p class=Normal> “Bhikkhus, se vocês forem perguntados pelos errantes de outras seitas, ‘Amigos, há essas três qualidades. Quais três? Paixão, raiva e delusão. Essas são as três qualidades. Agora qual é a diferença, qual é a distinção, qual é o fator que distingue essas três qualidades?’ – ao serem assim perguntados, vocês deveriam responder aos errantes de outras seitas deste modo: ‘Amigos, a paixão é menos censurável mas é difícil de ser removida. A raiva é mais censurável mas é mais fácil de ser removida. A delusão é muito censurável e é difícil de ser removida. [<a href="#N2">2</a>] </p>

<p class=Normal><a name="R2"> </a> “Se eles perguntarem: ‘Mas, amigos, qual é a razão, qual é a causa, para surgir a paixão que não surgiu, e para o incremento e fortificação da paixão que já surgiu?’ Vocês deveriam responder: ‘O sinal da beleza: para aquele que dá atenção sem sabedoria ao sinal da beleza, surge a paixão que não surgiu, e a paixão que já surgiu se incrementa e se fortifica.’ </p>

<p class=Normal> “Se eles perguntarem: ‘Mas, amigos, qual é a razão, qual é a causa, para surgir a raiva que não surgiu, e para o incremento e fortificação da raiva que já surgiu?’ Vocês deveriam responder: ‘O sinal do repulsivo: para aquele que dá atenção sem sabedoria ao sinal do repulsivo, surge a raiva que não surgiu, e a raiva que já surgiu se incrementa e se fortifica.’ </p>

<p class=Normal> “Se eles perguntarem: ‘Mas, amigos, qual é a razão, qual é a causa, para surgir a delusão que não surgiu, e para o incremento e fortificação da delusão que já surgiu?’ Vocês deveriam responder: ‘A atenção sem sabedoria: para aquele que dá atenção sem sabedoria, surge a delusão que não surgiu, e a delusão que já surgiu se incrementa e se fortifica.’ </p>

<p class=Normal> “Se eles perguntarem: ‘Mas, amigos, qual é a razão, qual é a causa, para que não surja a paixão que não surgiu, e para o abandono da paixão que já surgiu?’ Vocês deveriam responder: ‘O sinal do repulsivo: para aquele que dá atenção com sabedoria ao sinal do repulsivo, não surge a paixão que não surgiu, e a paixão que já surgiu é abandonada.’ </p>

<p class=Normal> “Se eles perguntarem: ‘Mas, amigos, qual é a razão, qual é a causa, para que não surja a raiva que não surgiu, e para o abandono da raiva que já surgiu?’ Vocês deveriam responder: ‘A libertação da mente através do amor bondade: para aquele que dá atenção com sabedoria para a libertação da mente através do amor bondade, não surge a raiva que não surgiu, e a raiva que já surgiu é abandonada.’ </p>

<p class=Normal> “Se eles perguntarem: ‘Mas, amigos, qual é a razão, qual é a causa, para que não surja a delusão que não surgiu, e para o abandono da delusão que já surgiu?’ Vocês deveriam responder: ‘A atenção com sabedoria: para aquele que dá atenção com sabedoria, não surge a delusão que não surgiu, e a delusão que já surgiu é abandonada.’” </p>

