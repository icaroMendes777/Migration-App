<p class=Normal><p class=Normal>Assim ouvi. Em certa ocasião, o Abençoado estava viajando em Magadha com uma grande sangha de bhikkhus, chegando até o vilarejo de Pataligama. Após ouvir: “O Abençoado acaba de chegar aqui." os discípulos leigos de Pataligama foram até o Abençoado e depois de cumprimentá-lo sentaram-se a um lado e disseram: “Que o Abençoado concorde em visitar o nosso salão de assembléias!” E o Abençoado concordou em silêncio. </p>



<p class=Normal>Então, quando eles viram que ele havia concordado, eles se levantaram dos seus assentos e depois de homenagear o Abençoado, mantendo-o à sua direita, foram até o salão de assembléias. Eles cobriram o salão completamente com coberturas e prepararam assentos, arranjaram um grande jarro com água e penduraram uma lamparina de azeite. Então, eles foram até o Abençoado e depois de cumprimentá-lo, ficaram em pé a um lado e disseram: “Venerável senhor, o salão de assembléias foi coberto completamente com coberturas e os assentos foram preparados, um grande jarro com água foi arranjado e uma lamparina de azeite foi pendurada. Agora é o momento para o Abençoado fazer aquilo que julgar adequado.”</p>



<p class=Normal>Então, o Abençoado se vestiu e tomando a sua tigela e o manto externo, foi com a Sangha dos bhikkhus até o salão de assembléias. Ao chegar, ele lavou os pés e depois entrou no salão e sentou-se próximo à coluna central olhando para o leste. E os bhikkhus lavaram os pés e depois entraram no salão e sentaram-se na parede do lado oeste olhando para o leste, com o Abençoado à sua frente.</p> 



<p class=Normal>E os discípulos leigos de Pataligama lavaram os pés e entraram no salão e sentaram-se na parede do lado leste olhando para o oeste, com o Abençoado à sua frente.</p>



<p class=Normal>Então, o Abençoado se dirigiu aos discípulos leigos de Pataligama: “O homem imoral, chefes de família, por ter decaído da virtude, enfrenta cinco perigos. Quais são eles? </p>



<p class=Normal>Em primeiro lugar ele sofre grande perda de posses devido à sua negligência.</p> 



<p class=Normal>Em segundo lugar, ele obtém má reputação devido à sua imoralidade e má conduta.</p> 



<p class=Normal>Em terceiro lugar, em qualquer assembléia que ele entre, quer seja de aristocratas, brâmanes, chefes de família ou contemplativos, ele se comporta de modo inseguro e tímido.</p> 



<p class=Normal>Em quarto lugar, ele morre confuso.</p> 



<p class=Normal>Em quinto lugar, com a dissolução do corpo, após a morte, ele reaparece num estado de privação, num destino infeliz, nos reinos inferiores, até mesmo no inferno. </p>



<p class=Normal>Esses, chefes de família, são os cinco perigos para aquele que é imoral.</p>



<p class=Normal>“Cinco bênçãos, chefes de família, são obtidas pelo homem íntegro através da prática da virtude. Quais são elas? </p>



<p class=Normal>Em primeiro lugar, devido à sua diligência ele obtém muitas posses.</p> 



<p class=Normal>Em segundo lugar, ele obtém boa reputação devido à sua moralidade e boa conduta.</p> 



<p class=Normal>Em terceiro lugar, em qualquer assembléia que ele entre, quer seja de aristocratas, brâmanes, chefes de família ou contemplativos, ele se comporta de modo seguro e autoconfiante. </p>



<p class=Normal>Em quarto lugar, ele não morre confuso.</p> 



<p class=Normal>Em quinto lugar, na dissolução do corpo, após a morte, ele renasce num destino feliz, no paraíso.</p> 



<p class=Normal>Essas são as cinco bênçãos para aquele que é íntegro através da prática da virtude.”</p>



<p class=Normal>Então, o Abençoado instruiu, motivou, estimulou e encorajou os discípulos leigos de Pataligama com um discurso do Dhamma, durante a maior parte da noite. Depois ele os dispensou, dizendo: “Chefes de família, a noite quase terminou. Agora é o momento para vocês fazerem o que julgarem adequado.”</p>



<p class=Normal>“Muito bem, venerável senhor,” eles responderam e então se levantaram dos seus assentos e depois de homenagear o Abençoado, mantendo-o à sua direita, partiram. E o Abençoado passou o restante da noite no salão de assembléias vazio.</p>



<p class=Normal>Agora, naquela ocasião, Sunidha e Vassakara, os ministros de Magadha, estavam construindo uma fortaleza em Pataligama como defesa contra os Vajjias. E ao mesmo tempo uma multidão de milhares de devas estava estabelecendo residência em Pataligama. E naquelas partes em que os devas poderosos se estabeleciam, eles faziam com que as mentes dos oficiais reais mais poderosos escolhessem aqueles lugares para se estabelecerem, e onde os devas intermediários e aqueles em posição inferior se estabeleciam, eles também faziam com que a mente dos oficiais com graduações correspondentes escolhessem aqueles lugares para se estabelecerem.</p>



<p class=Normal>E o Abençoado, por meio do olho divino, que ultrapassa o humano, viu aqueles milhares de devas se estabelecendo em Pataligama.</p> 



<p class=Normal>E, ao se levantar com o raiar do dia, ele disse para o venerável Ananda: “Ananda, quem está construindo uma fortaleza em Pataligama?” </p>



<p class=Normal>“Venerável senhor, Sunidha e Vassakara, os ministros de Magadha, estão construindo uma fortaleza contra os Vajjias.”</p>



<p class=Normal>“Ananda, é como se Sunidha e Vassakara tivessem se aconselhado com os devas do Trinta e Três para a construção da fortaleza em Pataligama. Eu vi por meio do olho divino que milhares de devas estão estabelecendo residência em Pataligama. E naquelas partes em que os devas poderosos se estabelecem, eles faziam com que as mentes dos oficiais reais mais poderosos escolham aqueles lugares para se estabelecerem, e onde os devas intermediários e aqueles em posição inferior se estabeleciam, eles também faziam com que a mente dos oficiais com graduações correspondentes escolham aqueles lugares para se estabelecerem.</p>



<p class=Normal>Ananda, até onde se estende o reino dos nobres, até onde se estendem as suas rotas de comércio, esta será a cidade principal, Pataliputta, dispersando as suas sementes por toda parte. E Pataliputta enfrentará três perigos: do fogo, da água e da dissensão interna.”</p>



<p class=Normal>Então, Sunidha e Vassakara foram até o Abençoado e depois de cumprimentá-lo ficaram em pé a um lado e disseram: “Que o venerável Gotama juntamente com a Sangha dos bhikkhus concorde em aceitar amanhã uma refeição oferecida por nós!” E o Abençoado concordou em silêncio.</p>



<p class=Normal>Então, sabendo que o Abençoado havia concordado, Sunidha e Vassakara foram para casa e fizeram com que fossem preparados vários tipos de boa comida. Quando tudo estava pronto eles anunciaram a hora para o Abençoado: “É hora, Mestre Gotama, a refeição está pronta.”</p> 



<p class=Normal>Então, o Abençoado se vestiu e, carregando a sua tigela e o manto externo, foi com a Sangha dos bhikkhus para a casa de Sunidha e Vassakara e sentou-se num assento que havia sido preparado. Então, com as próprias mãos Sunidha e Vassakara serviram e satisfizeram o Abençoado e os bhikkhus com os vários tipos de boa comida. Em seguida, quando o Abençoado havia terminado de comer e retirado a mão da sua tigela, Sunidha e Vassakara sentaram a um lado num assento mais baixo.</p>



<p class=Normal>Estando ali sentados, o Abençoado agradeceu com estes versos:</p>



	<p class=Normal style='margin-left:.75in'>“Aquele que é sábio<br>

	deveria alimentar os virtuosos da vida santa<br>

	onde quer que ele se encontre.<br>

	Tendo dado oferendas para os dignos,<br>

	ele comparte os seus méritos com os devas locais.<br>

	

	<p class=Normal style='margin-left:.75in'>Assim reverenciados, eles também o honram. <br>

	Amáveis tal qual uma mãe<br>

	em relação ao seu próprio filho, seu único filho.<br>

	

	<p class=Normal style='margin-left:.75in'>Aquele que assim desfruta da graça dos devas,<br>

	por eles é amado, encontra boa fortuna.”</p>

	

<p class=Normal>Depois disso o Abençoado levantou do seu assento e partiu. E então Sunidha e Vassakara seguiram de perto o Abençoado, dizendo: “Qualquer portão pelo qual o contemplativo Gotama saia hoje, esse será chamado de portão Gotama; e qualquer vau que ele use para cruzar o Ganges, esse será chamado vau Gotama.” </p>



<p class=Normal>E assim, o portão pelo qual o Abençoado saiu foi chamado portão Gotama. Então, o Abençoado chegou no Rio Ganges. E justamente então, o rio estava tão cheio que um corvo poderia beber nele. E algumas pessoas estavam procurando um barco, algumas buscavam uma balsa e outras estavam amarrando juncos para formar uma balsa para cruzar até o outro lado. Mas o Abençoado, com a mesma rapidez com que um homem forte estende o seu braço que está flexionado ou flexiona o seu braço que está estendido, desapareceu desta margem do Ganges e reapareceu com o seu grupo de bhikkhus na outra margem. E o Abençoado viu aquelas pessoas procurando um barco, em busca de uma balsa, amarrando juncos para formar uma balsa para cruzar até o outro lado. </p>



<p class=Normal>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p>



	<p class=Normal style='margin-left:.75in'>"Aqueles que atravessaram o vasto oceano,<br>

	- construindo uma ponte -<br>

	deixam para trás pântanos e manguezais,<br>

	

	enquanto os demais ainda amarram suas frágeis balsas,<br>

	os sábios para a outra margem já atravessaram.”</p>

