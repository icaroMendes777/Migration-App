<p class=Normal>Certa ocasião, o Venerável Maha Cunda estava entre os Cetis em Sanjatiya. Lá ele se dirigiu aos monges desta forma: “Amigos, bhikkhus!” </p>

<p class=Normal> “Sim, amigo,” os bhikkhus responderam. </p>

<p class=Normal>O Venerável Maha Cunda disse, “Amigos, é o caso em que bhikkhus devotos do Dhamma [<a href="#N1">1</a>] <a name="R1"> </a>desprezam os bhikkhus dedicados aos jhanas, dizendo: ‘Olhem para esses bhikkhus! Eles pensam ‘Nós somos meditadores, nós somos meditadores!’ e assim eles absorvem a si mesmos naquilo, absorvem mais, cismam, filosofam. [<a href="#N2">2</a>] <a name="R2"> </a>Mas por que, de fato, eles se absorvem? Com qual propósito eles se absorvem? Como eles se absorvem?’ Desse modo, os bhikkhus devotos do Dhamma não brilham e os bhikkhus dedicados aos jhanas não brilham. Essa não é a prática para a felicidade e bem-estar das massas, para o bem das massas, para a felicidade e bem-estar de devas e humanos. </p> 

<p class=Normal> “Depois, há o caso em que os bhikkhus dedicados aos jhanas desprezam os bhikkhus devotos do Dhamma, dizendo: ‘Esses bhikkhus dizem, “Nós somos expertos no Dhamma, nós somos expertos no Dhamma!” E por conseguinte eles são pretensiosos, orgulhosos e vaidosos; eles são falantes e volúveis. Desprovidos de atenção plena e plena consciência, e lhes falta a concentração; os seus pensamentos vagueiam e os seus sentidos são descontrolados. O que então faz com que eles sejam expertos no Dhamma, como e por que eles são expertos no Dhamma?’ Desse modo, os bhikkhus dedicados aos jhanas não brilham e os bhikkhus devotos do Dhamma não brilham. Essa não é a prática para a felicidade e bem-estar das massas, para o bem das massas, para a felicidade e bem-estar de devas e humanos. </p>

<p class=Normal> “Depois, há o caso em que os bhikkhus devotos do Dhamma só elogiam os bhikkhus devotos do Dhamma, mas não os bhikkhus dedicados aos jhanas. Desse modo, os bhikkhus devotos do Dhamma não brilham e os bhikkhus dedicados aos jhanas não brilham. Essa não é a prática para a felicidade e bem-estar das massas, para o bem das massas, para a felicidade e bem-estar de devas e humanos. </p>

<p class=Normal> “Depois, há o caso em que os bhikkhus dedicados aos jhanas só elogiam os bhikkhus dedicados aos jhanas, mas não os bhikkhus devotos do Dhamma. Desse modo, os bhikkhus dedicados aos jhanas não brilham e os bhikkhus devotos do Dhamma não brilham. Essa não é a prática para a felicidade e bem-estar das massas, para o bem das massas, para a felicidade e bem-estar de devas e humanos. </p>

<p class=Normal> “Portanto, amigos, vocês deveriam treinar assim: ‘Sendo bhikkhus devotos do Dhamma, nós elogiaremos os bhikkhus dedicados aos jhanas.’ Assim é como vocês deveriam treinar. Por que isso? Porque essas são pessoas extraordinárias, difíceis de serem encontradas no mundo, isto é, aquelas que tocam com o corpo o elemento imortal (nibbana). [<a href="#N3">3</a>] <a name="R3"> </a></p>

<p class=Normal><a name="R3"> </a> “E assim, amigos, vocês deveriam treinar: ‘Sendo bhikkhus dedicados aos jhanas, nós elogiaremos os bhikkhus devotos do Dhamma.’ Assim é como vocês deveriam treinar. Por que isso? Porque essas são pessoas extraordinárias, difíceis de serem encontradas no mundo, isto é, aquelas que através da sabedoria são capazes de compreender com clareza ensinamentos profundos.” </p>

