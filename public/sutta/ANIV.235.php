<p class=Normal> “Bhikkhus, esses quatro tipos de kamma foram diretamente compreendidos, verificados e anunciados por mim. Quais quatro? Há o kamma que é escuro com um resultado sombrio. Há o kamma que é claro com um resultado luminoso. Há o kamma que é escuro e claro com um resultado sombrio e luminoso. Há o kamma que é nem escuro nem claro com um resultado nem sombrio nem luminoso, que conduz ao fim de kamma. </p>
 
<p class=Normal> “E qual é o kamma que é escuro com um resultado sombrio? É o caso em que uma certa pessoa fabrica uma formação corporal prejudicial, fabrica uma formação verbal prejudicial, fabrica uma formação mental prejudicial. Tendo fabricado uma formação corporal prejudicial, tendo fabricado uma formação verbal prejudicial, tendo fabricado uma formação mental prejudicial, ela renasce num mundo com aflição. Ao renascer num mundo com aflição, ela é tocada por contatos aflitivos. Tocada por contatos aflitivos, ela experimenta sensações que são exclusivamente dolorosas, como aquelas experimentadas pelos seres no inferno. Isso é chamado kamma que é escuro com um resultado sombrio. </p>
 
<p class=Normal> “E qual é o kamma que é claro com um resultado luminoso? É o caso em que uma certa pessoa fabrica uma formação corporal não prejudicial, fabrica uma formação verbal não prejudicial, fabrica uma formação mental não prejudicial .... ela renasce num mundo sem aflição ... ela é tocada por contatos não aflitivos ...ela experimenta sensações que são exclusivamente prazerosas, como aquelas experimentadas pelos devas luminosos. Isso é chamado kamma que é claro com um resultado luminoso. </p>
 
<p class=Normal> “E qual é o kamma que é escuro e claro com um resultado sombrio e luminoso? É o caso em que uma certa pessoa fabrica uma formação corporal prejudicial e fabrica uma formação corporal não prejudicial, fabrica uma formação verbal prejudicial e fabrica uma formação verbal não prejudicial, fabrica uma formação mental prejudicial e fabrica uma formação mental não prejudicial .... ela renasce num mundo com aflição e sem aflição ... ela é tocada por contatos aflitivos e não aflitivos ...ela experimenta sensações que são dolorosas e prazerosas, como aquelas experimentadas pelos seres humanos, alguns devas e alguns seres nos mundos inferiores. Isso é chamado kamma que é escuro e claro com um resultado sombrio e luminoso. </p>
 
<p class=Normal> “E qual é o kamma que é nem escuro nem claro com um resultado nem sombrio nem luminoso, que conduz ao fim de kamma? Entendimento correto, pensamento correto, linguagem correta, ação correta, modo de vida correto, esforço correto, atenção plena correta, concentração correta. Isso é chamado kamma que é nem escuro nem claro com um resultado nem sombrio nem luminoso, que conduz ao fim de kamma. </p>
 
<p class=Normal> “Esses, bhikkhus são os quatro tipos de kamma que foram diretamente compreendidos, verificados e anunciados por mim." </p>

