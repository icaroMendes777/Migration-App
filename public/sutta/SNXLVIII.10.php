<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
há essas cinco faculdades. Quais cinco? A faculdade da convicção, a faculdade
da energia, a faculdade da atenção plena, a faculdade da concentração, a
faculdade da sabedoria.<span style="mso-spacerun: yes">  </span><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que, bhikkhus, é a
faculdade da convicção? Neste caso, bhikkhus, o nobre discípulo é uma pessoa
com fé, ele deposita fé na iluminação do Tathagata assim: ‘De
fato, o Abençoado é um arahant, perfeitamente iluminado, consumado no
verdadeiro conhecimento e conduta, bem-aventurado, conhecedor dos mundos, um
líder insuperável de pessoas preparadas para serem treinadas, mestre de devas
e humanos, desperto, sublime.’ </span>Essa é chamada a faculdade da convicção.</p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que, bhikkhus, é a
faculdade da energia? Neste caso, bhikkhus, o nobre discípulo permanece com a
sua energia desperta para o abandono de qualidades mentais prejudiciais e para
a obtenção de qualidades mentais benéficas; ele é decidido, firme no seu
esforço, sem se esquivar das suas responsabilidades com relação ao cultivo de
qualidades mentais benéficas. Ele é zeloso para que não surjam estados ruins e
prejudiciais que ainda não surgiram e ele se aplica, estimula a sua energia,
empenha a sua mente e se esforça. Ele é zeloso em abandonar estados ruins e
prejudiciais que já surgiram e ele se aplica, estimula a sua energia, empenha a
sua mente e se esforça. Ele é zeloso para que surjam estados benéficos que
ainda não surgiram e ele se aplica, estimula a sua energia, empenha a sua mente
e se esforça. Ele é zeloso para que haja a continuidade, o não desaparecimento,
o fortalecimento, o incremento e a realização através do desenvolvimento de
estados benéficos que já surgiram e ele se aplica, estimula a sua energia,
empenha a sua mente e se esforça. Essa é chamada a faculdade da energia.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que, bhikkhus, é a
faculdade da atenção plena? Neste caso, bhikkhus, o nobre discípulo tem atenção
plena, ele possui suprema atenção plena e discriminação, ele se recorda e lembra
aquilo que foi feito e dito há muito tempo. Ele permanece contemplando o corpo
como um corpo, ardente, plenamente consciente e com atenção plena, tendo
colocado de lado a cobiça e o desprazer pelo mundo. <a name=R6></a>Ele
permanece contemplando as sensações como sensações ... a mente como mente ...
os objetos mentais como objetos mentais, ardente, plenamente consciente e com
atenção plena, tendo colocado de lado a cobiça e o desprazer pelo mundo. Essa é
chamada a faculdade da atenção plena.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que, bhikkhus, é a
faculdade da concentração? Neste caso, bhikkhus, o nobre discípulo fazendo da
renúncia o seu objeto, alcança a concentração, alcança a unificação da mente.
Afastado dos prazeres sensuais, afastado das qualidades não hábeis, um bhikkhu
entra e permanece no primeiro jhana, que é caracterizado pelo pensamento aplicado
e sustentado, com o êxtase e felicidade nascidos do afastamento. Abandonando o
pensamento aplicado e sustentado, um bhikkhu entra e permanece no segundo
jhana, que é caracterizado pela segurança interna e perfeita unicidade da mente, sem o pensamento aplicado e sustentado, com o êxtase e felicidade nascidos da concentração. Abandonando o êxtase, um bhikkhu entra e permanece no terceiro jhana que é caracterizado pela felicidade sem o êxtase, acompanhada pela atenção plena, plena consciência e equanimidade, acerca do qual os nobres declaram: ‘Ele permanece numa estada feliz, equânime e plenamente atento.’ Com o completo desaparecimento da felicidade, um bhikkhu entra e permanece no quarto jhana, que possui nem felicidade nem sofrimento, com a atenção plena e a equanimidade purificadas. Essa é chamada a
faculdade da concentração.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que, bhikkhus, é a faculdade
da sabedoria? Neste caso, bhikkhus, o nobre discípulo é sábio; ele possui a completa compreensão da origem e da cessação, que é nobre e penetrante, que conduz ao
fim do sofrimento. Ele compreende como na verdade é que: ‘Isto é sofrimento …
Esta é a origem do sofrimento … Esta é a cessação do sofrimento … Esse é o
caminho da prática que conduz à cessação do sofrimento.’<span
style="mso-spacerun: yes">  </span>Essa é chamada a faculdade da sabedoria.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Essas, bhikkhus, são as
cinco faculdades.” <o:p></o:p></span></p>


