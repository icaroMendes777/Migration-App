<p class=Normal> <a name=N2></a>[2]    <i>Sarehi</i> pode significar tanto "com flechas" ou "com vozes", este versículo também pode ser traduzido: como fazem com gritos num elefante na batalha. O verso, portanto, produz duas interpretações igualmente válidas:</p>

<p class=Normal>     a) As pessoas atingindo o elefante com flechas (<i>sarehi</i>) são soldados inimigos, tentando derrubá-lo.</p>

<p class=Normal>     b) As pessoas estimulando o elefante com seus gritos e vozes (<i>sarehi</i>) são soldados lutando do mesmo lado, como o elefante, incitando-o para a luta.</p>

<p class=Normal>     O comentário dá apenas a interpretação da primeira. Mas se aceitarmos ambas as interpretações, o versículo contém um aviso mais útil duplo: Quando há uma controvérsia, cuidado com as pessoas descontroladas em ambos os lados. Aprender a suportar as palavras ofensivas daqueles do outro lado, que querem derrubá-lo; e as palavras ofensivas daqueles do seu lado, que tentam despertar a sua raiva para que você diga algo precipitado. [<a href="#R2">Retorna</a>] </p>

