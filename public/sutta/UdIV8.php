<p class=Normal> Assim, ouvi. Certa ocasião, o Abençoado estava em Savatthi, no Bosque de Jeta, no Parque de Anathapindika. Agora, naquela ocasião, o Abençoado era respeitado, reverenciado, honrado, venerado, e homenageado, e era um dos que obtinham mantos, comida esmolada, moradias, e medicamentos. A Sangha dos bhikkhus também era respeitada, reverenciada, honrada, venerada, e homenageada, e também obtinham mantos, comida esmolada, moradias, e medicamentos. Mas os errantes de outras seitas não eram respeitados, reverenciados, honrados, venerados, e homenageados, e também não obtinham mantos, comida esmolada, moradias, e medicamentos.</p>

<p class=Normal> Assim, os errantes de outras seitas - incapazes de suportar o respeito demonstrado ao Abençoado e à Sangha dos bhikkhus - foram até Sundari, a errante do sexo feminino e disseram: "Sundari, você faria algo em benefício dos seus familiares?"</p>

<p class=Normal> "O que posso fazer, mestres? O que sou capaz de fazer, mestres? Daria até mesmo a minha vida em benefício dos meus familiares!"</p>

<p class=Normal> "Nesse caso, Irmã, vá muitas vezes ao Bosque de Jeta."</p>

<p class=Normal> Respondendo, "Muito bem, mestres," Sundari foi muitas vezes ao Bosque de Jeta. Então, quando os errantes de outras seitas perceberam que muitas pessoas haviam visto Sundari ter ido muitas vezes ao Bosque de Jeta, eles a mataram e enterraram ali mesmo numa cova no fosso que circundava o Bosque de Jeta. Eles então foram até o rei Pasenadi de Kosala e, disseram: "Grande rei, não podemos encontrar Sundari."</p>

<p class=Normal> "Mas onde é que vocês suspeitam que ela esteja?"</p>

<p class=Normal> "No Bosque de Jeta, grande rei."</p>

<p class=Normal> "Então, nesse caso, façam uma busca no Bosque de Jeta."</p>

<p class=Normal> Então, aqueles errantes de outras seitas, tendo feito uma busca no Bosque de Jeta, desenterraram o corpo que tinha sido enterrado numa cova no fosso, colocaram-no sobre uma maca, e o levaram para Savatthi passando de rua em rua, de cruzamento em cruzamento, agitando a indignação das pessoas dizendo: "Olhem, mestres, a obra dos contemplativos discípulos do filho dos Sakyas. Esses contemplativos, os discípulos do filho dos Sakyas, não têm vergonha, são imorais, com má conduta, mentirosos, profanos. Eles afirmam estar praticando o Dhamma, que vivem uma vida harmoniosa, praticam a vida santa, que falam a verdade, que são virtuosos, e com boa conduta, mas eles não têm o status de um contemplativo, eles não têm o status de um brâmane; o seu status de contemplativo foi destruído. Onde está o seu status de contemplativo? Onde está o seu status de brâmane? Eles perderam o seu status de contemplativo, eles perderam o seu status de brâmane. Como pode um homem, tendo desfrutado do seu prazer masculino, matar uma mulher?" </p>

<p class=Normal> Devido a isso, quando as pessoas viam bhikkhus em Savathi elas os insultavam, ofendiam, provocavam, e irritavam com insultos e palavras grosseiras, dizendo: "Esses contemplativos discípulos do filho dos Sakyas, não têm vergonha, são imorais, com má conduta, mentirosos, profanos ... Como pode um homem, tendo desfrutado do seu prazer masculino, matar uma mulher?"</p>

<p class=Normal> Então, ao amanhecer, um certo número de bhikkhus se vestiram e tomando a tigela e manto externo, foram para Savathi para esmolar alimentos. Depois de haver esmolado em Savatthi e de haver retornado, após a refeição, eles foram até o Abençoado e depois de cumprimentá-lo sentaram a um lado e disseram: "No momento, venerável senhor, quando as pessoas em Savatthi vêm os bhikkhus, elas os insultam, ofendem, provocam, e irritam com insultos e palavras grosseiras, dizendo: "Esses contemplativos discípulos do filho dos Sakyas, não têm vergonha, são imorais, com má conduta, mentirosos, profanos ... Como pode um homem, tendo desfrutado do seu prazer masculino, matar uma mulher?"</p>

<p class=Normal> "Bhikkhus, essa comoção não irá durar muito. Irá continuar por apenas mais sete dias, e depois de sete dias irá desaparecer. Portanto, bhikkhus, se as pessoas os insultarem, ofenderem, provocarem, e irritarem com insultos e palavras grosseiras, vocês devem responder com estes versos: [<a href="#N1">1</a>]</p>

<p class=Normal> <a name=R1></a>O falso acusador vai para o inferno,<br>
também aquele que nega o que tenha feito.<br>
Ambos depois se tornam iguais,<br>
pessoas vis no mundo além.</p>

<p class=Normal> Assim, os bhikkhus memorizaram esses versos na presença do Abençoado, e quando as pessoas os insultavam eles respondiam com esses versos. </p>

<p class=Normal> Então aquelas pessoas pensaram: "Esses contemplativos, discípulos do filho dos Sakyas não fizeram isso; isso não foi cometido por eles. Esses contemplativos, discípulos do filho dos Sakyas estão afirmando a sua inocência. E a comoção não continuou por muito tempo. A comoção durou apenas sete dias, e depois de sete dias desapareceu. </p>

<p class=Normal> Então um número de bhikkhus foram até o Abençoado e depois de cumprimentá-lo sentaram a um lado e disseram: "É admirável! É maravilhoso venerável senhor! Como isso foi bem antecipado pelo Abençoado:  'Bhikkhus, essa comoção não irá durar muito. Irá continuar por apenas mais sete dias, e depois de sete dias irá desaparecer.' Venerável senhor, essa comoção desapareceu."</p>

<p class=Normal>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p>

<p class=Normal style='margin-left:.75in'><a name=R2></a> Pessoas descontroladas apunhalam com palavras<br>
como fazem com flechas num elefante na batalha. [<a href="#N2">2</a>] <br>
Ao ouvir palavras grosseiras<br>
um bhikkhu deve tolerá-las sem raiva. </p>

