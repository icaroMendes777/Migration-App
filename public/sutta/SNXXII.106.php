<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, eu ensinarei para vocês as coisas que devem ser
completamente compreendidas, a completa compreensão e a pessoa que compreendeu
completamente. </span>Ouçam e prestem muita atenção àquilo que eu vou dizer.” –
“Sim, venerável senhor,” os bhikkhus responderam. O Abençoado disse o seguinte:</p>


<p class=Normal>“E o que<span lang=PT-BR style='mso-ansi-language:PT-BR'>,
bhikkhus, são as coisas que devem ser completamente compreendidas? A forma,
bhikkhus, é algo que deve ser completamente compreendido. A sensação ...
percepção ... formações volitivas ... consciência é algo que deve ser completamente
compreendido. Essas são as coisas que devem ser completamente compreendidas. <o:p></o:p></span></p>


<p class=Normal>“E o que<span lang=PT-BR style='mso-ansi-language:PT-BR'>,
bhikkhus, é a completa compreensão? A destruição da cobiça, a destruição da
raiva, a destruição da delusão: isso é chamado completa compreensão.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E quem,
bhikkhus, é a pessoa que compreendeu completamente? Deve ser dito: o arahant, o
venerável com tal nome e de tal clã. Essa é chamada a pessoa que compreendeu
completamente.”<b style='mso-bidi-font-weight:normal'><o:p></o:p></b></span></p>


