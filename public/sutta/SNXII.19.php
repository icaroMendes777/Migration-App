<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em Savathi.
“Bhikkhus, para o tolo, obstruído pela ignorância e aprisionado pelo desejo,
este corpo surgiu. Agora, existem ambos, este corpo e a
mentalidade-materialidade (nome e forma) externa. Aqui, na dependência dessa dualidade, existe
o contato nas seis bases. Há apenas seis bases, através das quais pelo contato
o tolo se torna sensível ao prazer e à dor.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
para o sábio, obstruído pela ignorância e aprisionado pelo desejo, este corpo
surgiu. Agora, existem ambos, este corpo e a mentalidade-materialidade (nome e forma) externa.
Aqui, na dependência dessa dualidade, existe o contato nas seis bases. Há
apenas seis bases, através das quais pelo contato o sábio se torna sensível ao
prazer e à dor. Então, bhikkhus, qual a diferença, qual a distinção, qual o
fator que distingue a pessoa sábia do tolo?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, os nossos ensinamentos têm o Abençoado como origem, como guia e como
refúgio. Seria bom se o Abençoado pudesse explicar o significado dessas
palavras. Tendo ouvido do Abençoado, os bhikkhus o recordarão.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Nesse
caso, bhikkhus, ouçam e prestem muita atenção àquilo que eu vou dizer.” – “Sim,
venerável senhor,” os bhikkhus responderam. O Abençoado disse o seguinte: <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
para o tolo, obstruído pela ignorância e aprisionado pelo desejo, este corpo
surgiu. Essa ignorância não foi abandonada pelo tolo; esse desejo não foi
destruído. Por que isso? O tolo não viveu a vida santa para a completa
destruição do sofrimento. Assim, na dissolução do corpo, ele estará destinado a
um (novo) corpo. Destinado a um corpo, ele não estará completamente livre do
nascimento, envelhecimento, morte, tristeza, lamentação, dor, angústia e
desespero. Eu lhes digo, ele não estará completamente livre do sofrimento.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
para o sábio, obstruído pela ignorância e aprisionado pelo desejo, este corpo
surgiu. Essa ignorância foi abandonada pelo sábio; esse desejo foi destruído.
Por que isso? O sábio viveu a vida santa para a completa destruição do
sofrimento. Assim, na dissolução do corpo, ele não estará destinado a um (novo)
corpo. Não destinado a um corpo, ele estará completamente livre do nascimento,
envelhecimento, morte, tristeza, lamentação, dor, angústia e desespero. Eu lhes
digo, ele estará completamente livre do sofrimento.<a name=T2></a><a name=N6></a><a
name=N18></a><o:p></o:p></span></p>


