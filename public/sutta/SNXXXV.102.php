<p class=Normal> “Bhikkhus, tudo aquilo que não é seu, abandonem-no. Ao abandoná-lo, isso irá conduzir ao seu bem-estar e felicidade por muito tempo. E o que, bhikkhus, não é seu? O olho não é seu, abandonem-no. Ao abandoná-lo, isso irá conduzir ao seu bem-estar e felicidade por muito tempo. As formas não são suas ... A consciência no olho não é sua ... O contato no olho não é seu ... Qualquer sensação que surja, tendo o contato no olho como condição - experimentada como prazer, dor ou nem prazer, nem dor, também não é sua, abandonem-na. Ao abandoná-la, isso irá conduzir ao seu bem-estar e felicidade por muito tempo.

“O ouvido não é seu ... A mente não é sua ... Os objetos mentais não são seus ... A consciência na mente não é sua ... O contato na mente não é seu ... Qualquer sensação que surja, tendo o contato na mente como condição - experimentada como prazer, dor ou nem prazer, nem dor, também não é sua, abandonem-na. Ao abandoná-la, isso irá conduzir ao seu bem-estar e felicidade por muito tempo.</p>

