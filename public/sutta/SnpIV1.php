dinheiro, gado, cavalos,<br>
serviçais, empregados,<br>
mulheres, parentes,<br>
será subjugado pelas paixões, <br>
os perigos o esmagarão,<br>
e a dor irá segui-lo,<br>
igual à água entrando num barco naufragando. <o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
