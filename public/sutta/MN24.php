<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião o Abençoado estava em Rajagaha, no Bambual, no Santuário
dos Esquilos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. Então um
número de bhikkhus da terra natal (do Abençoado), [</span><a href="#N1"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>1</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>] <a name=R1></a>que lá haviam passado o retiro
das chuvas, foram até o Abençoado e depois de cumprimentá-lo sentaram a um
lado. O Abençoado perguntou: “Bhikkhus, quem na minha terra natal é estimado
pelos<span style='mso-spacerun:yes'>  </span>bhikkhus de lá, pelos seus
companheiros na vida santa, desta forma: ‘Tendo ele mesmo poucos desejos, ele
fala aos bhikkhus sobre a parcimônia com os desejos; estando ele mesmo
satisfeito, ele fala aos bhikkhus sobre a satisfação; estando ele mesmo
afastado, ele fala aos bhikkhus sobre o afastamento; estando ele mesmo distante
da sociedade, ele fala aos bhikkhus sobre o distanciamento da sociedade; sendo
ele mesmo energético, ele fala aos bhikkhus sobre estimular a energia; tendo ele
mesmo a virtude realizada, ele fala aos bhikkhus sobre a realização da virtude;
tendo ele mesmo realizado a concentração, ele fala aos bhikkhus sobre a
realização da concentração; tendo ele mesmo realizado a sabedoria, ele fala aos
bhikkhus sobre a realização da sabedoria; tendo ele mesmo realizado a
libertação, ele fala aos bhikkhus sobre a realização da libertação; tendo ele
mesmo realizado o conhecimento e visão da libertação, ele fala aos bhikkhus
sobre a realização do conhecimento e visão; [</span><a href="#N2"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>2</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>] <a name=R2></a>ele é aquele que aconselha,
informa, instrui, motiva, estimula e encoraja os seus companheiros na vida
santa’.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, o venerável Punna Mantaliputta é assim estimado na terra natal do
Abençoado pelos bhikkhus de lá, pelos seus companheiros na vida santa.” [</span><a
href="#N3"><span lang=PT-BR style='mso-ansi-language:PT-BR'>3</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=R3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>3. Agora naquela ocasião o venerável Sariputta estava sentado próximo do
Abençoado. Então o venerável Sariputta pensou o seguinte: “É um ganho para o
venerável Punna Mantaliputta, é um grande ganho para ele que os seus sábios
companheiros na vida santa o elogiem ponto por ponto na presença do Mestre. Talvez
um dia desses possamos encontrar com o venerável Punna Mantaliputta e ter uma
conversa com ele.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. Então,
depois que o Abençoado havia estado em Rajagaha pelo tempo que queria, ele saiu
caminhando em direção a Savatthi. Caminhando em etapas ele por fim chegou em
Savatthi, indo para o<span style='mso-spacerun:yes'>  </span>Bosque de Jeta, no
Parque de Anathapindika.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. O
venerável Punna Mantaliputta ouviu: “O Abençoado chegou em Savatthi e está no
Bosque de Jeta, no Parque de Anathapindika.” Então o venerável Punna
Mantaliputta arrumou a sua moradia e tomando o manto externo e a tigela saiu
caminhando em direção a Savatthi. Caminhando em etapas ele por fim chegou em
Savatthi e foi até o Bosque de Jeta, no Parque de Anathapindika, para ver o
Abençoado. Depois de cumprimentá-lo ele sentou a um lado e o Abençoado o
instruiu, motivou, estimulou e encorajou com um discurso do Dhamma. Então o
venerável Punna Mantaliputta, instruído, motivado, estimulado e encorajado pelo
discurso do Dhamma do Abençoado, satisfeito e contente com as palavras do
Abençoado, levantou do seu assento e depois de homenageá-lo, mantendo-o à sua
direita, foi para o Bosque dos Cegos para passar o resto do dia.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. Então um
certo bhikkhu foi até o venerável Sariputta e lhe disse: “Amigo Sariputta, o
bhikkhu Punna Mantaliputta a quem você sempre se referiu de forma elogiosa, foi
instruído, motivado, estimulado e encorajado pelo Abençoado com um discurso do
Dhamma; depois de ficar satisfeito e contente com as palavras do Abençoado ele
levantou do seu assento e depois de homenagear o Abençoado, mantendo-o à sua
direita, ele foi para o Bosque dos Cegos para passar o resto do dia.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. Então o
venerável Sariputta pegou depressa o seu pano para sentar e seguiu de perto o
venerável Punna Mantaliputta, mantendo a cabeça dele à vista. Então o venerável
Punna Mantaliputta entrou no Bosque dos Cegos e sentou à sombra de uma árvore para
passar o resto do dia. O venerável Sariputta também entrou no Bosque dos Cegos
e sentou à sombra de uma árvore para passar o resto do dia.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. Então,
quando havia anoitecido, o venerável Sariputta se levantou da meditação, foi
até o venerável Punna Mantaliputta, e ambos se cumprimentaram. Quando a
conversa amigável e cortês havia terminado, ele sentou a um lado e disse para o
venerável Punna Mantaliputta:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. “A vida
santa é vivida sob o Abençoado, amigo?” – “Sim, amigo.” – “Mas, amigo, é com o
propósito de purificação da virtude que a vida santa é vivida sob o Abençoado?”
– “Não, amigo.” –<span style='mso-spacerun:yes'>  </span>“Então, é com o
propósito de purificação da mente que a vida santa é vivida sob o Abençoado?” –
“Não, amigo.” –<span style='mso-spacerun:yes'>  </span>“Então, é com o
propósito de purificação do entendimento que a vida santa é vivida sob o
Abençoado?” – “Não, amigo.” – “Então, é com o propósito de purificação da
superação da dúvida que a vida santa é vivida sob o Abençoado?” – “Não, amigo.”
– “Então, é com o propósito de purificação <span
style='mso-spacerun:yes'> </span>do conhecimento e visão do que é o caminho e
do que não é o caminho que a vida santa é vivida sob o Abençoado?” – “Não,
amigo.” –<span style='mso-spacerun:yes'>  </span>“Então, é com o propósito de
purificação <span style='mso-spacerun:yes'> </span>do conhecimento e visão da
prática que a vida santa é vivida sob o Abençoado?” – “Não, amigo.” –<span
style='mso-spacerun:yes'>  </span>“Então, é com o propósito de purificação <span
style='mso-spacerun:yes'> </span>do conhecimento e visão que a vida santa é
vivida sob o Abençoado?” – “Não, amigo.”[</span><a href="#N4"><span lang=PT-BR
style='mso-ansi-language:PT-BR'>4</span></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=R4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>10. “Amigo, quando perguntado: ‘Mas amigo,<span
style='mso-spacerun:yes'>  </span>é com o propósito de purificação da virtude
que a vida santa é vivida sob o Abençoado?’ você respondeu: ‘Não, amigo.’ Quando
perguntado: ‘Então é com o propósito de purificação da mente ... purificação do
entendimento ... purificação <span style='mso-spacerun:yes'> </span>da superação
da dúvida ... purificação <span style='mso-spacerun:yes'> </span>do conhecimento
e visão do que é o caminho e do que não é o caminho ... purificação <span
style='mso-spacerun:yes'> </span>do conhecimento e visão da prática ...
purificação <span style='mso-spacerun:yes'> </span>do conhecimento e visão que
a vida santa é vivida sob o Abençoado?’ você respondeu: ‘Não, amigo.’ Com qual
propósito então, amigo, a vida santa é vivida sob o Abençoado?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigo, é
com o propósito de parinibbana sem apego que a vida santa é vivida sob o
Abençoado.” [</span><a href="#N5"><span lang=PT-BR style='mso-ansi-language:
PT-BR'>5</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=R5></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>11. “Mas, amigo, a purificação da virtude é parinibbana sem apego?” –
“Não amigo.” – “Então a purificação da mente é parinibbana sem apego?” –
“Não, amigo.” - “Então a purificação do entendimento é parinibbana sem
apego?” – “Não amigo.” –<span style='mso-spacerun:yes'>  </span>“Então a
purificação <span style='mso-spacerun:yes'> </span>da superação da dúvida é
parinibbana sem apego?” – “Não amigo.” –<span style='mso-spacerun:yes'> 
</span>“Então a purificação <span style='mso-spacerun:yes'> </span>do
conhecimento e visão do que é o caminho e do que não é o caminho é parinibbana sem apego?” – “Não amigo.” –<span style='mso-spacerun:yes'> 
</span>“Então a purificação <span style='mso-spacerun:yes'> </span>do
conhecimento e visão da prática é parinibbana sem apego?” – “Não amigo.” –
“Então a purificação <span style='mso-spacerun:yes'> </span>do conhecimento e
visão é parinibbana sem apego?” – “Não amigo.” – “Mas, amigo, parinibbana
sem apego pode ser alcançado sem esses estados?” – “Não, amigo.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. “Quando
perguntado: ‘Mas, amigo, a purificação da virtude é parinibbana sem apego?’
você respondeu: ‘Não, amigo.’ Quando perguntado: ‘Então a purificação da mente
... purificação do entendimento ... purificação <span
style='mso-spacerun:yes'> </span>da superação da dúvida ... purificação <span
style='mso-spacerun:yes'> </span>do conhecimento e visão do que é o caminho e
do que não é o caminho ... purificação <span
style='mso-spacerun:yes'> </span>do conhecimento e visão da prática ...
purificação <span style='mso-spacerun:yes'> </span>do conhecimento e visão é
parinibbana sem apego?’<span style='mso-spacerun:yes'>  </span>você
respondeu: ‘Não, amigo.’ E quando perguntado: ‘Mas, amigo, parinibbana sem
apego pode ser alcançado sem esses estados?’ Você respondeu: ‘Não, amigo.’Mas
como, amigo, deve ser entendido o significado dessas afirmações?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. “Amigo,
se o Abençoado tivesse descrito a purificação da virtude como parinibbana sem
apego, ele teria descrito aquilo que ainda está acompanhado pelo apego como
parinibbana sem apego. Se o Abençoado tivesse descrito a purificação da mente
... purificação do entendimento ... purificação <span
style='mso-spacerun:yes'> </span>da superação da dúvida ... purificação <span
style='mso-spacerun:yes'> </span>do conhecimento e visão do que é o caminho e
do que não é o caminho ... purificação <span
style='mso-spacerun:yes'> </span>do conhecimento e visão da prática ...
purificação <span style='mso-spacerun:yes'> </span>do conhecimento e visão como
parinibbana sem apego, ele teria descrito aquilo que ainda está acompanhado
pelo apego como parinibbana sem apego.[</span><a href="#N6"><span lang=PT-BR
style='mso-ansi-language:PT-BR'>6</span></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>] <a name=R6></a>E se parinibbana sem apego fosse realizado sem esses
estados, então uma pessoa comum realizaria parinibbana pois uma pessoa comum
está desprovida desses estados.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14. “Com
relação a isso, amigo, eu explicarei com um símile pois alguns sábios compreendem
o significado de um enunciado através de um símile. Suponha que o rei Pasenadi
de Kosala estivesse vivendo em Savatthi e tivesse um assunto urgente que
resolver em Saketa, e que entre Savatthi e Saketa houvessem sete carruagens de
revezamento mantidas preparadas à espera do rei. Então o rei Pasenadi de
Kosala, saindo de Savatthi pelo portão interno do palácio, montaria na primeira
carruagem e, por meio da primeira carruagem ele chegaria até a segunda
carruagem; então ele desmontaria da primeira carruagem e montaria na segunda
carruagem e, por meio da segunda carruagem ele chegaria até a terceira
carruagem ... por meio da terceira carruagem ele chegaria até a quarta
carruagem ... por meio da quarta carruagem ele chegaria até a quinta carruagem ... por
meio da quinta carruagem ele ele chegaria até a sexta carruagem ... por meio da
sexta carruagem ele chegaria até a sétima carruagem, e por meio da sétima
carruagem ele chegaria até o portão interno do palácio em Saketa. Então, tendo
chegado ao portão interno do palácio, os seus amigos e conhecidos, seus
paisanos e parentes, perguntariam: ‘Senhor, você veio de Savatthi até o portão
interno do palácio em Saketa por meio desta carruagem?’ Como então deveria o
rei Pasenadi de Kosala responder de forma a responder corretamente?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“De forma a
responder corretamente, amigo, ele deveria responder assim: ‘Aqui, enquanto
estava em Savatthi tive um assunto urgente que resolver em Saketa, e entre
Savatthi e Saketa haviam sete carruagens de revezamento mantidas preparadas à
minha espera. Então saindo de Savatthi pelo portão interno do palácio, montei
na primeira carruagem e, por meio da primeira carruagem cheguei até a segunda
carruagem; então desmontei da primeira carruagem e montei na segunda carruagem
e, por meio da segunda carruagem cheguei até a terceira
carruagem ... quarta ... quinta ... sexta ... sétima carruagem e por meio da sétima
carruagem cheguei até o portão interno do palácio em Saketa.’ De forma a
responder corretamente ele deveria responder assim.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. “Da
mesma forma, amigo, a purificação da virtude tem o propósito de realizar a
purificação da mente; a purificação da mente tem o propósito de realizar a
purificação do entendimento; a purificação do entendimento tem o propósito de
realizar a purificação <span style='mso-spacerun:yes'> </span>da superação da
dúvida; a purificação <span style='mso-spacerun:yes'> </span>da superação da
dúvida tem o propósito de realizar a purificação <span
style='mso-spacerun:yes'> </span>do conhecimento e visão do que é o caminho e
do que não é o caminho; a purificação <span style='mso-spacerun:yes'> </span>do
conhecimento e visão do que é o caminho e do que não é o caminho tem o
propósito de realizar a purificação <span style='mso-spacerun:yes'> </span>do
conhecimento e visão da prática; a purificação <span
style='mso-spacerun:yes'> </span>do conhecimento e visão da prática tem o
propósito de realizar a purificação <span style='mso-spacerun:yes'> </span>do
conhecimento e visão; a purificação <span style='mso-spacerun:yes'> </span>do
conhecimento e visão tem o propósito de realizar o parinibbana sem apego. É com
o propósito de parinibbanaa sem apego que a vida santa é vivida sob o
Abençoado.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16. Quando
isso foi dito, o venerável Sariputta perguntou<span style='mso-spacerun:yes'> 
</span>ao venerável Punna Mantaliputta : “Qual é o nome do venerável, e como os
companheiros na vida santa o conhecem?” [</span><a href="#N7"><span lang=PT-BR
style='mso-ansi-language:PT-BR'>7</span></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=R7></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Meu nome é Punna, amigo, e meus companheiros na vida santa me conhecem
como Mantaliputta.” “É maravilhoso, amigo, é admirável! Cada questão profunda
foi respondida, ponto por ponto, pelo venerável Punna Mantaliputta como um
discípulo esclarecido que compreende os Ensinamentos do Mestre da forma
correta. É um ganho para os seus companheiros na vida santa, é um grande ganho
para eles ter a oportunidade de ver e honrar o venerável Punna Mantaliputta . Mesmo
se fosse carregando o venerável Punna Mantaliputta em uma almofada sobre as
suas cabeças que os seus companheiros teriam a oportunidade de vê-lo e
honrá-lo, seria um ganho para eles, um grande ganho para eles. E é um ganho
para nós, um grande ganho para nós que tenhamos a oportunidade de ver e honrar
o venerável Punna Mantaliputta.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17. Quando
isso foi dito, o venerável Punna Mantaliputta<span style='mso-spacerun:yes'> 
</span>perguntou<span style='mso-spacerun:yes'>  </span>ao venerável Sariputta:
“Qual é o nome do venerável, e como os companheiros na vida santa o conhecem?” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Meu nome é
Upatissa, amigo, e meus companheiros na vida santa me conhecem como Sariputta.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“De fato,
amigo, não sabíamos que estávamos conversando com o venerável Sariputta, o
discípulo que é igual ao próprio Mestre. [</span><a href="#N8"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>8</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>] <a name=R8></a>Se soubéssemos que era o
venerável Sariputta, não teríamos falado tanto. É maravilhoso, amigo, é
admirável! Cada questão profunda foi colocada, ponto por ponto, pelo venerável
Sariputta como um discípulo esclarecido que compreende os Ensinamentos do
Mestre da forma correta. É um ganho para os seus companheiros na vida santa, é
um grande ganho para eles ter a oportunidade de ver e honrar o venerável
Sariputta. Mesmo se fosse carregando o venerável Sariputta em uma almofada sobre
as suas cabeças que os seus companheiros teriam a oportunidade de vê-lo e
honrá-lo, seria um ganho para eles, um grande ganho para eles. E é um ganho
para nós, um grande ganho para nós que tenhamos a oportunidade de ver e honrar
o venerável Sariputta.”<o:p></o:p></span></p>



