style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=N4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[4] Embora estas sete purificações (<i>satta visuddhi</i>) sejam
mencionadas em um outro ponto do Cânone em Pali (no <a href="DN34.php#T2_2_2">DN
34.2.2 (2)</a>, com a adição de duas mais: purificação da sabedoria e
purificação da libertação), é curioso que elas não são analisadas como um
conjunto em nenhum lugar dos Nikayas; e isso é ainda mais intrigante ao
observar que ambos eminentes discípulos neste sutta aparentam reconhecê-las
como um grupo fixo de categorias doutrinárias. Esse esquema de sete tipos
forma, no entanto, a armação para todo o Visuddhimagga (Vsm), que define, com
base nos comentários tradicionais, os diferentes estágios do desenvolvimento da
meditação de concentração e da meditação de insight.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>De forma
sucinta “purificação da virtude” (<i>silavisuddhi</i>) é a contínua manutenção
dos preceitos morais que a pessoa tenha tomado, explicado no Vsm através da
referência ao treinamento moral de um bhikkhu como a “purificação quádrupla da
virtude.” “Purificação da mente “ (<i>cittavisuddhi</i>) é a superação dos
cinco obstáculos através da realização da concentração de acesso e dos jhanas. “Purificação
do entendimento” (<i>ditthivisuddhi</i>) é o entendimento que define a natureza
dos cinco agregados que constituem um ser vivo. “Purificação da superação da
dúvida” (<i>kankhavitaranavisuddhi</i>) é o entendimento da condicionalidade. “Purificação
do conhecimento e visão do que é o caminho e do que não é o caminho” (<i>maggamaggañanadassanavisuddhi</i>)
é a correta discriminação entre o caminho falso das experiências que causam
êxtase e regozijo e o caminho verdadeiro do insight da impermanência, sofrimento
e não-eu. “Purificação do conhecimento e visão da prática” (<i>patipadañanadassanavisuddhi</i>)
é a série ascendente do saber por meio do insight até os caminhos
supramundanos. E a “Purificação do conhecimento e visão” (<i>ñanadassanavisuddhi</i>)
são os caminhos supramundanos.[</span><a href="#R4"><span lang=PT-BR
style='mso-ansi-language:PT-BR'>Retorna</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>]</span><span lang=PT-BR style='font-size:11.0pt;
mso-bidi-font-size:12.0pt;mso-ansi-language:PT-BR'><o:p></o:p></span></p>


<p class=Normal><a name=N5></a> [5] </span><span lang=PT-BR
style='mso-ansi-language:PT-BR'>MA aponta <i>anupadanaparinibbana</i> como <i>appaccayaparinibbana</i>,
“Parinibbana que não tem condição,” explicando que <i>upadana</i> tem dois
significados: apego (<i>gahana</i>), como ocorre na usual descrição dos quatro
tipos de apego; e condição (<i>paccaya</i>), como ilustrado neste trecho. Os
comentaristas explicam “Parinibbana sem apego” ou como o fruto do estado de
arahant, porque não pode ser agarrado por nenhum dos quatro tipos de apego; ou
como Nibbana o incondicionado, porque não surge através de alguma condição. [</span><a
