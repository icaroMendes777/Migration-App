<p class=Tit1 align=center style='text-align:center'>Brahmayacana Sutta </Tit1>
<p class=Tit1 align=center style='text-align:center'>O Pedido de um Brahma </Tit1>

<p class=Normal align=center style='text-align:center'><span lang=PT-BR style='font-size:
