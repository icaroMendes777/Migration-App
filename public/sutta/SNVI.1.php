<p class=Normal style='margin-left:.75in'>


<p class=Normal>  Assim ouvi. [<a href="#N1">1</a>] <a name=R1></a>Certa
ocasião, o Abençoado estava em Uruvela às margens do rio Neranjara ao pé da
figueira-dos-pagodes, pouco tempo depois de ter alcançado a perfeita
iluminação. Então, enquanto o Abençoado estava só em isolamento, um pensamento
surgiu na sua mente: “Este Dhamma que eu alcancei é profundo, difícil de ver e
difícil de compreender, pacífico e sublime, que não pode ser alcançado através
do mero raciocínio, ele é sutil, para ser experimentado pelos sábios.<span
style='mso-spacerun:yes'>  </span>Mas, esta população se delicia com a adesão,
está excitada com a adesão, desfruta da adesão. É difícil para uma população como
esta ver esta verdade, isto é, a condicionalidade isto/aquilo e a origem
dependente. E também é difícil de ver esta verdade, isto é, o cessar de todas
as formações, o abandono de todas aquisições, o fim do desejo, desapego,
cessação, Nibbana. Se eu fosse ensinar o Dhamma, os outros não me entenderiam e
isso seria fatigante, problemático para mim .” Então, estes versos nunca antes
ouvidos, ocorreram ao Abençoado: <o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'> “Basta com a idéia de ensinar o Dhamma <br>

que até para mim foi difícil alcançar;<br>

pois ele nunca será entendido<br>

por aqueles que vivem com a cobiça e a raiva.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'> Aqueles tingidos pela cobiça, envoltos na
escuridão<span style='mso-spacerun:yes'>    </span><br>

nunca irão discernir este Dhamma difícil de ser
compreendido<br>

que vai contra a torrente do mundo,<br>

sutil, profundo e difícil de ser visto.”<o:p></o:p></span></p>


<p class=Normal> Pensando dessa forma, a mente do Abençoado tendia à
inação ao invés do ensino do Dhamma. <o:p></o:p></span></p>


<p class=Normal> Então o Brahma Sahampati, soube com a mente dele o
pensamento na mente do Abençoado e pensou: “O mundo estará perdido, o mundo
estará destruído, já que a mente do Tathagata, um arahant, perfeitamente iluminado,
se inclina à inação ao invés do ensino do Dhamma.” Então, com a mesma rapidez
com que um homem forte pode estender o seu braço flexionado ou flexionar o seu
braço estendido, Brahma Sahampati desapareceu do mundo de Brahma e apareceu na
frente do Abençoado. Ele arrumou o seu manto externo sobre o ombro e juntou as
mãos numa reverenciosa saudação, dizendo: “Venerável senhor, que o Abençoado
ensine o Dhamma, que o Iluminado ensine o Dhamma. Há seres com pouca poeira
sobre os olhos que estão decaindo por não ouvir o Dhamma. Há aqueles que
entenderão o Dhamma.” Depois de dizer isso, Brahma Sahampati disse ainda mais:<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'> “Em Magadha surgiram até agora<br>

ensinamentos contaminados formulados por
aqueles que ainda estão poluídos.<br>

Abram as portas para o Imortal! Que eles ouçam<br>

o Dhamma que o Imaculado encontrou.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'> Tal como alguém que esteja no pico de uma
montanha<br>

é capaz de ver todas as pessoas embaixo,<br>

da mesma forma, Oh sábio, sábio que tudo vê,<br>

suba ao palácio do Dhamma.<br>

Que o Conquistador da Tristeza inspecione esta
raça humana,<br>

engolfada na tristeza, subjugada pelo
nascimento e envelhecimento.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'> Levante-se, Oh herói, vitorioso na batalha!<br>

Oh líder da caravana, sem dívidas, saia pelo
mundo.<br>

Ensine o Dhamma, Oh Abençoado:<br>

Existem aqueles que irão compreender.”<o:p></o:p></span></p>


<p class=Normal> Então o Abençoado, tendo ouvido o pedido de Brahma e
por compaixão pelos seres, inspecionou o mundo com o olho de um Buda.
Inspecionando o mundo com o olho de um Buda, ele viu seres com pouca poeira
sobre os olhos e com muita poeira sobre os olhos, com faculdades aguçadas e com
faculdades embotadas, com boas qualidades e com más qualidades, fáceis de serem
ensinados e difíceis de serem ensinados e alguns que permaneciam sentindo medo
e responsabilidade pelo outro mundo. Tal como num lago com flores de lótus
azuis ou vermelhas ou brancas, algumas flores de lótus nascem e crescem na água
e prosperam imersas na água sem sair fora da água, enquanto que algumas outras
flores de lótus nascem e crescem na água e pousam sobre a superfície da água, e
ainda, algumas outras flores de lótus nascem e crescem na água e sobem acima do
nível da água permanecendo sem serem molhadas pela água; assim também,
inspecionando o mundo com o olho de um Buda, ele viu seres com pouca poeira
sobre os olhos e com muita poeira sobre os olhos, com faculdades aguçadas e com
faculdades embotadas, com boas qualidades e com más qualidades, fáceis de serem
ensinados e difíceis de serem ensinados e alguns que permaneciam sentindo medo
e responsabilidade pelo outro mundo. Então ele respondeu ao Brahma Sahampati em
versos:<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'> “Para eles estão abertas as portas para o
Imortal,<br>

que
aqueles com ouvidos mostrem agora a sua fé.<br>

Pensando
que seria problemático, Oh Brahma,<br>

eu
não quis falar o Dhamma sutil e sublime.”<o:p></o:p></span></p>


<p class=Normal> Então o Brahma Sahampati pensou: ‘Eu criei a
oportunidade para que o Abençoado ensine o Dhamma.’ E depois de homenageá-lo,
mantendo-o à sua direita, ele então desapareceu.</span><span lang=PT-BR
style='mso-ansi-language:PT-BR'><o:p></o:p></span></p>

