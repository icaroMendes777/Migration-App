<p class=Normal>Assim ouvi. Em certa ocasião o Abençoado estava perambulando por Kosala com uma grande sangha de bhikkhus. Então, deixando a estrada, o Abençoado caminhou até uma árvore, e ao chegar ele sentou num assento que havia sido preparado.</p> 



<p class=Normal>Então, um certo vaqueiro foi até o Abençoado, o homenageou e sentou a um lado. Enquanto ele estava sentado a um lado o Abençoado o instruiu, motivou, estimulou e encorajou com um discurso do Dhamma.</p>



<p class=Normal>Aquele vaqueiro - instruído, motivado, estimulado e encorajado com um discurso do Dhamma - disse em seguida ao Abençoado: “Que o Abençoado junto com a Sangha dos bhikkhus concorde em aceitar a refeição de amanhã.” </p>



<p class=Normal>O Abençoado consentiu em silêncio.</p>



<p class=Normal>Sabendo que o Abençoado havia consentido, o vaqueiro depois de homenagear o Abençoado, mantendo-o à sua direita, partiu.</p>



<p class=Normal>Então, quando a noite terminou, o vaqueiro - tendo preparado em sua própria residência uma grande quantidade de arroz doce com leite, coalhada e manteiga fresca - anunciou a hora para o Abençoado: “É hora, venerável senhor, a refeição está pronta.” </p>



<p class=Normal>Então, ao amanhecer, o Abençoado vestiu o manto interno e - tomando a sua tigela e o manto externo - foi junto com a Sangha dos bhikkhus para a residência do vaqueiro. Ao chegar, o Abençoado sentou num assento que havia sido preparado. Então, com as suas próprias mãos, o vaqueiro serviu e satisfez a Sangha dos bhikkhus liderada pelo Abençoado com arroz doce com leite, coalhada e manteiga fresca. Quando o Abençoado havia terminado de comer e removeu a mão da tigela, o vaqueiro tomou um assento, a um lado. Enquanto o vaqueiro estava sentado a um lado o Abençoado o instruiu, motivou, estimulou e encorajou com um discurso do Dhamma e, em seguida, levantou-se do assento e partiu.</p>



<p class=Normal>Agora, não muito tempo depois da partida do Abençoado, aquele vaqueiro foi assassinado por um certo homem na fronteira entre dois vilarejos. Uma grande sangha de bhikkhus então foi até o Abençoado, o homenageou e sentou a um lado. Enquanto sentados, eles disseram ao Abençoado: "O vaqueiro que, mais cedo hoje serviu e satisfez a Sangha dos bhikkhus liderada pelo Abençoado com arroz doce com leite, coalhada e manteiga fresca, morreu. Dizem que ele foi assassinado por um certo homem na fronteira entre dois vilarejos.”</p>



<p class=Normal>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p>



	<p class=Normal style='margin-left:.75in'>"O que quer que um inimigo<br>

	possa fazer a um inimigo,<br>

	ou que um rival ao seu rival,<br>

	uma mente ou pensamento mal intencionado<br>

	pode causar ainda mais dano.”</p>


