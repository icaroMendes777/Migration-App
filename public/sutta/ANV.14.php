<p class=Normal> “Bhikkhus, há esses cinco poderes: o poder da convicção, o poder da energia, o poder da atenção plena, o poder da concentração, o poder da sabedoria. </p>

<p class=Normal> (1) "Qual, bhikkhus, é o poder da convicção? Aqui, um nobre discípulo possui fé. Ele deposita fé na iluminação do Tathagata assim: 'O Abençoado é um arahant, perfeitamente iluminado, consumado no verdadeiro conhecimento e conduta, bem-aventurado, conhecedor dos mundos, um líder insuperável de pessoas preparadas para serem treinadas, mestre de devas e humanos, desperto, sublime.' Esse é chamado o poder da convicção. </p>

<p class=Normal> (2) "Qual é o poder da energia? Aqui, um nobre discípulo permanece com a sua energia desperta para o abandono de qualidades mentais prejudiciais e para a obtenção de qualidades mentais benéficas; ele é decidido, firme no seu esforço, sem se esquivar das suas responsabilidades com relação ao cultivo de qualidades mentais benéficas. Esse é chamado o poder da energia. </p>

<p class=Normal> (3) "Qual é o poder da atenção plena? Aqui, um nobre discípulo tem atenção plena, ele possui suprema atenção plena e discriminação, ele se recorda e lembra aquilo que foi feito e dito há muito tempo. Esse é chamado o poder da atenção plena. </p>

<p class=Normal> (4) "Qual é o poder da concentração? Aqui, afastado dos prazeres sensuais, afastado das qualidades não hábeis, um bhikkhu entra e permanece no primeiro jhana, que é caracterizado pelo pensamento aplicado e sustentado, com o êxtase e felicidade nascidos do afastamento. Abandonando o pensamento aplicado e sustentado, um bhikkhu entra e permanece no segundo jhana, que é caracterizado pela segurança interna e perfeita unicidade da mente, sem o pensamento aplicado e sustentado, com o êxtase e felicidade nascidos da concentração. Abandonando o êxtase, um bhikkhu entra e permanece no terceiro jhana que é caracterizado pela felicidade sem o êxtase, acompanhada pela atenção plena, plena consciência e equanimidade, acerca do qual os nobres declaram: ‘Ele permanece numa estada feliz, equânime e plenamente atento.’ Com o completo desaparecimento da felicidade, um bhikkhu entra e permanece no quarto jhana, que possui nem felicidade nem sofrimento, com a atenção plena e a equanimidade purificadas. Esse é chamado o poder da concentração. </p>

<p class=Normal> (5) "Qual é o poder da sabedoria? Aqui, o nobre discípulo é sábio; ele possui a completa compreensão da origem e da cessação, que é nobre e penetrante, que conduz ao fim do sofrimento. Esse é chamado o poder da sabedoria. </p>

<p class=Normal>"Esses, bhikkhus, são os cinco poderes." </p>

