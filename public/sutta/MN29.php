<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
esta vida santa, bhikkhus, não tem o ganho, honraria e fama como seu benefício,
ou a perfeição da virtude como seu benefício, ou a perfeição da concentração
como seu benefício, ou o conhecimento e visão como seu benefício. Mas é a<span
style="mso-spacerun: yes">  </span>libertação inabalável da mente que é o
objetivo desta vida santa, o seu cerne e o seu fim.” [<a href="#N4">4</a>]<o:p></o:p></span></p>


