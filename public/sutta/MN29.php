... ele vive em sofrimento. Esse bhikkhu é chamado aquele que tomou a casca
externa da vida santa e se deteve antes do final.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4.
