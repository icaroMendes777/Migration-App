perfeição da concentração, desenvolve a negligência, se torna negligente e
sendo negligente, ele vive em sofrimento.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Suponham
