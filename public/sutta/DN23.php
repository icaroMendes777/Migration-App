felicidade. Mas aquele que voltou com um monte de ouro satisfez os seus pais,
sua esposa e filhos, seus amigos e companheiros, e ele obteve para si mesmo
muita alegria e felicidade.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Príncipe,
você fala como o carregador de cânhamo na minha história. Príncipe, abandone
essa idéia perniciosa, deixe-a de lado! Não permita que ela lhe traga desgraça
e sofrimento por muito tempo!”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>30. “Eu
estava satisfeito e contente com o primeiro símile do venerável Kassapa, mas
queria ouvir as suas respostas perspicazes a estas perguntas, porque supus que
era um adversário digno. Magnífico, venerável Kassapa! Magnífico, venerável
Kassapa! O venerável Kassapa esclareceu o Dhamma de várias formas, como se
tivesse colocado em pé o que estava de cabeça para baixo, revelasse o que
estava escondido, mostrasse o caminho para alguém que estivesse perdido ou
segurasse uma lâmpada no escuro para aqueles que possuem visão pudessem ver as
formas. Venerável Kassapa, eu busco refúgio no Abençoado, no Dhamma e na Sangha
dos bhikkhus. Que o venerável Kassapa me aceite como discípulo leigo que buscou refúgio para o
resto da vida. E, venerável Kassapa, eu gostaria de oferecer um grande
sacrifício. Instrua-me, venerável Kassapa, como isso deve ser feito para o meu
bem-estar e felicidade por muito tempo.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>31.
“Príncipe, quando um sacrifício, onde são mortos touros, bois ou novilhos,
bodes ou carneiros, ou vários outros tipos de seres vivos, é realizado; [<a
href="#N9">9</a>] <a name=R9></a>e os participantes têm entendimento incorreto,
pensamento incorreto, linguagem incorreta, ação incorreta, modo de vida incorreto,
esforço incorreto, atenção plena incorreta e concentração incorreta, então,
esse sacrifício não traz grandes frutos ou benefícios, ele não é muito
brilhante e não tem luminosidade. Suponha Príncipe, que um agricultor fosse
para a floresta com um arado e sementes, e lá, num pedaço de terra não
cultivado, com solo pobre, onde os tocos não tivessem sido desenraizados, ele
semeasse sementes que estivessem quebradas, apodrecidas, velhas, arruinadas
pelo vento e pelo calor, e que elas fossem plantadas no solo da forma
inadequada, e que os devas da chuva não mandassem as chuvas no momento apropriado
– essas sementes germinariam, se desenvolveriam e cresceriam, e o agricultor
obteria uma colheita abundante?” - “Não, venerável Kassapa.”.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
Príncipe, ocorre o mesmo com um sacrifício no qual são mortos touros ... os
participantes têm entendimento incorreto ... concentração incorreta. Mas quando
nenhuma dessas criaturas é morta e os participantes têm entendimento correto,
pensamento correto, linguagem correta, ação correta, modo de vida correto,
esforço correto, atenção plena correta e concentração correta, então aquele
sacrifício traz grandes frutos ou benefícios, ele é muito brilhante e tem
luminosidade. Suponha, Príncipe, que um agricultor fosse para a floresta com um
arado e sementes, e lá, num pedaço de terra bem cultivado com solo rico, do
qual os tocos tivessem sido desenraizados, ele semeasse sementes que não
estivessem quebradas, apodrecidas, ou velhas, arruinadas pelo vento e pelo
calor, e que elas fossem plantadas no solo de forma adequada e que os devas da
chuva mandassem as chuvas no momento apropriado – essas sementes germinariam, se
desenvolveriam e cresceriam, e o agricultor obteria uma colheita abundante?” -
“Sim, venerável Kassapa.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Do mesmo
modo, Príncipe, um sacrifício no qual não são mortos touros, ... os
participantes têm entendimento correto, ... concentração correta, então, esse
sacrifício traz grandes frutos ou benefícios, ele é muito brilhante e tem
luminosidade.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>32. Então,
o Príncipe Payasi estabeleceu uma caridade para contemplativos e Brâmanes,
peregrinos, mendigos e para os necessitados. A comida servida era arroz de
segunda com mingau azedo e também a roupa oferecida era grosseira e áspera. Um
jovem brâmane chamado Uttara foi colocado como responsável pela distribuição.
Em relação a isso ele dizia: “Através desta caridade estou associado ao
Príncipe Payasi neste mundo, mas não no mundo que está além.”.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>E o
Príncipe Payasi depois de ouvir aquelas palavras pediu que ele viesse à sua
presença e aí perguntou-lhe se ele havia dito aquilo. “Sim, senhor.” - “Mas
porque você disse esse tipo de coisa? Amigo Uttara, nós, que desejamos ganhar
mérito, não esperamos uma recompensa pela nossa caridade?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas,
senhor, a comida que você dá - arroz de segunda com mingau azedo – você não
iria querer tocá-la nem com o pé, quanto menos comê-la! E as roupas grosseiras
e ásperas – você não iria querer tocá-las nem com o pé, quanto menos vesti-las!
Senhor, você é amável e generoso conosco, mas como podemos reconciliar tal
amabilidade e generosidade com a má vontade e mesquinhez?” - “Muito bem Uttara,
então, providencie para que a comida oferecida seja aquela que eu como e as
roupas, as que eu visto.” - “Muito bem, senhor,” Uttara disse e assim ele fez. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>E o
Príncipe Payasi, porque havia estabelecido aquela caridade com má vontade e não com
as suas próprias mãos, sem o cuidado apropriado, como se de modo descuidado
jogasse algo fora, renasceu com a dissolução do corpo, após a morte, na
companhia dos Quatro Grandes Reis, na mansão vazia Serisaka. Mas Uttara, que
havia dado a caridade sem má vontade, com as suas próprias mãos e com o interesse
apropriado, não de modo descuidado, com a dissolução do corpo, após a morte,
renasceu num destino feliz, no paraíso, na companhia dos devas do Trinta e
Três.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>33. Agora,
naquela ocasião, o venerável Gavampati tinha o hábito de ir até a mansão vazia
Serisaka para a sua sesta do meio-dia. E Payasi foi até o venerável Gavampati e
depois de cumprimentá-lo ficou em pé a um lado. E o venerável Gavampati disse o
seguinte: “Quem é você, amigo?” - “Senhor, eu sou o Príncipe Payasi.” - “Amigo,
você não é aquele que costumava dizer: ‘Não existe outro mundo, não há seres
que renascem espontaneamente, não existe fruto ou resultado de ações boas ou
más’?” - “Sim, senhor, eu sou aquele que costumava dizer isso, mas fui convertido
dessa idéia perniciosa pelo nobre Kumara-Kassapa.” - “E onde renasceu o jovem
Brâmane Uttara, que estava a cargo da distribuição da sua caridade?”.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Senhor,
ele que deu a caridade sem má vontade ... renasceu num destino feliz, no paraíso, na
companhia dos devas do Trinta e Três, mas eu, que dei com má vontade ... renasci
aqui na mansão vazia Serisaka. Senhor, por favor, quando você retornar para o
mundo humano, diga para as pessoas darem sem má vontade ... e diga-lhes onde o
Príncipe Payasi e o jovem Brâmane Uttara renasceram.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>34. E assim
o Venerável Gavampati, ao retornar ao mundo humano, declarou: “Vocês devem dar
sem má vontade, com as próprias mãos e com o cuidado apropriado, não de modo
descuidado. O Príncipe Payasi não fez isso e com a dissolução do corpo, após a
morte, ele renasceu na companhia dos Quatro Grandes Reis na mansão vazia de
Serisaka, enquanto que o administrador da sua caridade, o jovem Brâmane Uttara,
que deu sem má vontade, com as suas próprias mãos e com o cuidado apropriado, não
de modo descuidado, renasceu na companhia dos devas do Trinta e Três.”<o:p></o:p></span></p>



