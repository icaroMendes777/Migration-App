<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião, o Venerável Kumara-Kassapa [<a href="#N1">1</a>] <a
name=R1></a>estava perambulando pela região de Kosala, com uma grande Sangha de
bhikkhus, com quinhentos bhikkhus, e ele acabou chegando numa cidade denominada
Setavya. Ele ficou ao norte de Setavya na Floresta de Simsapa. [<a href="#N2">2</a>]
<a name=R2></a>Agora, naquela ocasião, o Príncipe Payasi vivia em Setavya, uma
propriedade real com muitos habitantes, rica em pastagens, árvores, rios e
grãos, uma concessão real, uma doação sagrada que lhe havia sido dada pelo rei
Pasenadi de Kosala.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. Uma
idéia perniciosa havia surgido na mente do Príncipe Payasi: “Não existe outro
mundo, não há seres que renascem espontaneamente, não existe fruto ou resultado
de ações boas ou más.” [<a href="#N3">3</a>] <a name=R3></a>E ao mesmo tempo,
os brâmanes chefes de família de Setavya ouviram: “O contemplativo
Kumara-Kassapa, um discípulo do contemplativo Gotama, estava perambulando pela
região de Kosala, com uma grande Sangha de bhikkhus, com quinhentos bhikkhus;
ele chegou a Setavya e está ao norte de Setavya na Floresta de Simsapa; e
acerca desse venerável Kassapa existe essa boa reputação: ‘Ele é estudado,
experiente, sábio, bem informado, um excelente orador, capaz de dar boas
respostas, um venerável, um arahant. É bom poder encontrar alguém tão nobre.’”
Assim, os brâmanes chefes de família de Setavya, saíram de Setavya pelo portão
ao norte em grupos e bandos e se dirigiram para a Floresta de Simsapa.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. Agora,
naquela ocasião, o Príncipe Payasi havia se retirado para o andar superior do
seu palácio para a sesta. Então, ele viu os brâmanes chefes de família de
Setavya, saindo de Setavya em grupos e bandos e se dirigindo para a Floresta de
Simsapa. Ao vê-los ele perguntou ao seu ministro porque estava ocorrendo
aquilo. O ministro disse: “Senhor, é o contemplativo Kumara-Kassapa, um
discípulo do contemplativo Gotama ... e acerca desse venerável Kassapa existe
essa boa reputação ... É por isso que eles estão indo vê-lo.” - “Muito bem, então
ministro, vá até os brâmanes chefes de família de Setavya e diga: ‘Senhores, o
Príncipe Payasi diz o seguinte: “Por favor, esperem, senhores. O Príncipe
Payasi também irá ver o contemplativo Kassapa.’” Esse contemplativo
Kumara-Kassapa ensinará esses tolos e inexperientes Brâmanes chefes de família
de Setavya que existe um outro mundo, que há seres que renascem espontaneamente
e que existe fruto ou resultado de ações boas ou más. Mas esse tipo de coisas
não existe.” - “Muito bem, venerável senhor,” disse o ministro, e assim ele
comunicou a mensagem.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. O
Príncipe Payasi, junto com os Brâmanes chefes de família de Setavya, foi até a
Floresta de Simsapa onde estava o Venerável Kumara-Kassapa e ambos se
cumprimentaram. Quando a conversa cortês e amigável havia terminado ele sentou
a um lado. E alguns homenagearam o Venerável Kumara-Kassapa e sentaram a um
lado; alguns trocaram saudações corteses com ele e após a troca de saudações
sentaram a um lado; alguns ajuntaram as mãos em respeitosa saudação e sentaram
a um lado; alguns anunciaram o seu nome e clã e sentaram a um lado. Alguns
permaneceram em silêncio e sentaram a um lado. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. Então, o
Príncipe Payasi disse para o Venerável Kumara-Kassapa: “Venerável Kassapa, eu
acredito nesta doutrina e idéia: não existe outro mundo, não há seres que
renascem espontaneamente, não existe fruto ou resultado de ações boas ou más.”<br>
“Muito bem, Príncipe, eu nunca vi ou ouvi uma doutrina ou idéia como essa que
você afirma. E assim, Príncipe, eu o questionarei sobre isso e você responda
como considerar adequado. O que você pensa, Príncipe, o sol e a lua estão neste
mundo ou noutro, eles são devas ou humanos?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
Kassapa, eles estão noutro mundo e eles são devas, não humanos.” “Da mesma
forma, Príncipe, você deveria considerar: ‘Existe um outro mundo, há seres que
renascem espontaneamente, existe fruto ou resultado de ações boas ou más.’” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. “Não
importa o que você diga com relação a isso, Venerável Kassapa, eu ainda penso
que não existe outro mundo ...” - “Você tem alguma justificativa para essa
afirmação, Príncipe?” - “Eu tenho, Venerável Kassapa.” - “Qual é, Príncipe?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável Kassapa,
eu tenho amigos, companheiros e parentes, que tiram a vida de outros seres,
tomam o que não é dado, se comportam de forma imprópria em relação aos prazeres
sensuais, dizem mentiras, empregam a linguagem maliciosa, grosseira e frívola,
que são cobiçosos, possuem má vontade e têm entendimento incorreto. Com o tempo
eles se enfermam e sofrem adoentados. E quando tenho certeza de que eles não
irão se recuperar, eu vou até eles e digo: ‘Há certos contemplativos e Brâmanes
que declaram e acreditam que aqueles que tiram a vida ... têm entendimento
incorreto, irão, com a dissolução do corpo, após a morte, renascer num estado
de privação, num destino infeliz, nos reinos inferiores, até mesmo no <span
style='mso-bidi-font-weight:bold'>inferno</span>. Agora, vocês fizeram essas
coisas e se aquilo que aqueles contemplativos e Brâmanes dizem for verdade, é
para lá que vocês irão. Agora, se com a dissolução do corpo vocês renascerem
num estado de privação, ... venham até mim e declarem que existe um outro
mundo, que há seres que renascem espontaneamente, que existe fruto ou resultado
de ações boas ou más. Vocês, senhores, são responsáveis e confiáveis e aquilo
que vocês virem será como se eu mesmo tivesse visto, assim será.’ Mas embora
eles concordem, eles não vêm para me contar e tampouco mandam um mensageiro.
Essa, Venerável Kassapa, é a razão para a afirmação: ‘Não existe outro mundo,
não há seres que renascem espontaneamente, não existe fruto ou resultado de
ações boas ou más.’”<o:p></o:p></span></p>



<p class=Normal><a name=T7></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>7. “Quanto a isso, Príncipe, eu lhe farei uma pergunta em retorno.
Responda como quiser. O que você pensa, Príncipe? Suponha que trouxessem à sua
presença um ladrão pego em flagrante e dissessem: ‘Este homem, Senhor, é um
ladrão pego em flagrante. Sentencie-o a qualquer punição que você desejar.’ E
você poderá dizer: ‘Prendam os braços deste homem para trás com uma corda
forte, raspem a cabeça dele e conduzam-no ao rufar dos tambores pelas ruas e
praças e para fora da cidade pelo portão sul, lá, dêem um fim nele aplicando a
pena capital, decepando a sua cabeça!’ E eles, dizendo: ‘Muito bem’,
assentindo, poderiam ... conduzi-lo pelo portão sul e lá decepar a cabeça dele.
Agora, se aquele ladrão dissesse para os carrascos: ‘Estimados carrascos, nesta
cidade ou vilarejo eu tenho amigos, companheiros e parentes, por favor esperem
até que eu os tenha visitado,’ ele obteria o seu desejo? Ou eles simplesmente
decepariam a cabeça daquele ladrão tagarela?” - “Ele não obteria o seu desejo,
Venerável Kassapa. Eles simplesmente decepariam a cabeça dele.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
Príncipe, aquele ladrão não conseguiria sequer que os seus carrascos esperassem
enquanto ele visitava os seus amigos e parentes. Então, como poderiam os seus
amigos, companheiros e parentes que praticaram todas essas ações inábeis, tendo
morrido e renascido no inferno, persuadir os guardiões do inferno, dizendo:
‘Estimados guardiões do inferno, por favor, esperem enquanto relatamos ao
Príncipe Payasi que existe um outro mundo, que há seres que renascem
espontaneamente, que existe fruto ou resultado de ações boas ou más?’ Por
conseguinte, Príncipe, admita que existe um outro mundo ...<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. “Não
importa o que você diga com relação a isso, Venerável Kassapa, eu ainda penso
que não existe outro mundo...” - “Você tem alguma outra justificativa para essa
afirmação, Príncipe?” - “Eu tenho, Venerável Kassapa.” - “Qual é, Príncipe?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
Kassapa, eu tenho amigos ... que se abstêm de tirar a vida de outros seres, de
tomar o que não é dado, que não se comportam de forma imprópria em relação aos
prazeres sensuais, não dizem mentiras, não empregam a linguagem maliciosa,
grosseira e frívola, que não são cobiçosos, não possuem má vontade e têm
entendimento correto. Com o tempo eles se enfermam ... E quando tenho certeza
que eles não irão se recuperar, eu vou até eles e digo: ‘Há certos
contemplativos e Brâmanes que declaram e acreditam que aqueles que se abstêm de
tirar a vida ... têm entendimento correto, irão, com a dissolução do corpo, após
a morte, renascer num destino feliz, no paraíso. Agora, vocês fizeram essas
coisas e se aquilo que aqueles contemplativos e Brâmanes dizem for verdade, é
para lá que vocês irão. Agora, se com a dissolução do corpo vocês renascerem
num destino feliz ... venham até mim e declarem que existe um outro mundo ...
Vocês, senhores, são responsáveis e confiáveis e aquilo que vocês virem será
como se eu mesmo tivesse visto, assim será.’ Mas embora eles concordem, eles
não vêm para me contar e tampouco mandam um mensageiro. Essa, Venerável
Kassapa, é a razão para afirmar: ‘Não existe outro mundo ...’” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. “Muito
bem, então, Príncipe, eu explicarei com um símile, pois alguns sábios
compreendem o significado de um enunciado através de um símile. Suponha que um
homem caísse de cabeça numa fossa e você dissesse para os seus auxiliares:
‘Tirem aquele homem da fossa!’ E eles dissessem: ‘Muito bem,’ e assim fizessem.
Então, você lhes diria que limpassem por completo o corpo daquele homem com
raspadeiras feitas de bambu e depois lhe dessem um banho triplo com marga
amarela. Depois você lhes diria que<span style='mso-spacerun:yes'> 
</span>friccionassem o corpo dele com óleo e depois o limpassem três vezes com
sabão em pó fino. E você os instruiria para que aparassem o cabelo e barba dele
e o enfeitassem com finas grinaldas, perfumes e roupas. Por fim, você lhes diria 
que o conduzissem ao seu palácio e permitissem que ele desfrutasse dos
prazeres dos cinco sentidos, e assim fariam eles. O que você pensa, Príncipe?
Aquele homem, depois de banhado, com a barba e cabelo aparados, engrinaldado e
adornado, vestido de branco, depois de ter sido conduzido ao palácio,
desfrutado e gozado dos prazeres dos cinco sentidos, gostaria de ser novamente
mergulhado naquela fossa?” - “Não, Venerável Kassapa.” - “Porque não?” - “Porque
aquela fossa é imunda, fétida, horrível, nojenta e em geral é assim
considerada.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Exatamente
da mesma maneira, Príncipe, os seres humanos são imundos, fétidos, horríveis e
nojentos e em geral são assim considerados pelos devas. Então, porque deveriam
os seus amigos ... que não praticaram ações inábeis ... (igual ao verso 8), e que
com a dissolução do corpo, após a morte, renasceram num destino feliz, no
paraíso, retornar e dizer: ‘Existe um outro mundo ... existe fruto ou resultado
de ações boas ou más?’ Por conseguinte, Príncipe, admita que existe um outro
mundo ...<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. “Não
importa o que você diga com relação a isso, Venerável Kassapa, eu ainda penso
que não existe outro mundo ...” - “Você tem alguma outra justificativa para essa
afirmação, Príncipe?” - “Eu tenho, Venerável Kassapa.” - “Qual é, Príncipe?” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
Kassapa, eu tenho amigos que se abstêm ... de dizer mentiras, do álcool, vinho e
outros embriagantes. Com o tempo eles se enfermam ... ‘Há certos
contemplativos e Brâmanes que declaram e acreditam que aqueles que se abstêm de
tirar a vida ... e do álcool, vinho e outros embriagantes ... irão, com a
dissolução do corpo, após a morte, renascer num destino feliz, no paraíso,
como companheiros dos devas do Trinta e Três ...’ Mas embora eles concordem,
eles não vêm para me contar e tampouco mandam um mensageiro. Essa, Venerável
Kassapa, é a razão para afirmar: ‘Não existe outro mundo ...’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. “Quanto
a isso, Príncipe, eu lhe farei uma pergunta em retorno. Responda como quiser.
Aquilo, Príncipe, que para os humanos são cem anos para os devas do Trinta e
Três é um dia e uma noite. Trinta dessas noites resultam num mês, doze desses
meses, num ano e mil desses anos é o tempo de vida dos devas do Trinta e Três.
Agora, suponha que eles pensassem: ‘Depois que gozarmos dos prazeres dos
sentidos por dois ou três dias iremos até Payasi para contar-lhe que existe um
outro mundo, que há seres que renascem espontaneamente, que existe fruto ou
resultado de ações boas ou más,’ eles fariam isso?” - “Não, venerável Kassapa,
porque nós já teríamos morrido há muito tempo. Mas, venerável Kassapa, quem lhe
disse que os devas do Trinta e Três existem e que eles vivem por tanto tempo?
Eu não acredito que os devas do Trinta e Três existam ou que vivam por tanto
tempo.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Príncipe,
imagine um homem que seja cego de nascimento e que não possa ver objetos claros
ou escuros ou objetos azuis, amarelos, vermelhos, ou carmesins, não possa ver o
plano e o rugoso, não possa ver as estrelas e a lua. Ele poderia dizer: ‘Não
existem objetos claros e escuros e ninguém que possa vê-los ... não existe o sol
ou a lua e ninguém que possa vê-los. Eu não tenho consciência disso e portanto,
isso não existe.’ Ele estaria falando da forma correta, Príncipe?” - “Não,
venerável Kassapa. Existem objetos claros e escuros ..., existe um sol e uma
lua e qualquer um que diga: ‘Eu não tenho consciência disso e portanto isso não
existe’ não estaria falando da forma correta.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bem,
Príncipe, parece que a sua resposta é igual à resposta do homem cego ao me
perguntar como sei dos devas do Trinta e Três e da sua longevidade. Príncipe, o
outro mundo não pode ser visto da forma como você pensa, através do olho
físico. Príncipe, aqueles contemplativos e Brâmanes que buscam nos lugares
isolados na floresta, um local que seja tranqüilo, com pouco ruído – eles ali
permanecem afastados, decididos, ardentes, purificando o olho divino e com esse
olho divino purificado que supera o poder do olho humano, eles tanto vêm este
mundo como o mundo que está além e os seres que renascem espontaneamente.
Assim, Príncipe, é como o outro mundo pode ser visto e não da forma como você
pensa, através do olho físico. Por conseguinte, Príncipe, admita que existe um
outro mundo, que há seres que renascem espontaneamente, que existe fruto ou
resultado de ações boas ou más.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. “Não
importa o que você diga com relação a isso, Venerável Kassapa, eu ainda penso que
não existe outro mundo ...” - “Você tem alguma outra justificativa para essa afirmação,
Príncipe?” - “Eu tenho, Venerável Kassapa.” - “Qual é, Príncipe?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bem,
Venerável Kassapa, eu vejo aqui alguns contemplativos e Brâmanes que observam a
virtude e são bem comportados, que desejam viver, não querem morrer, que
desejam o prazer e abominam a dor. E me parece que se esses bons contemplativos
e Brâmanes que são tão virtuosos e bem comportados sabem que depois da morte
eles desfrutarão de uma situação melhor, então essas boas pessoas deveriam
agora mesmo tomar veneno, tomar uma faca e se matar, enforcar-se ou pular de um
penhasco. Mas, embora eles tenham esse conhecimento, eles ainda assim desejam
viver, não querem morrer, eles desejam o prazer e abominam a dor. Essa, Venerável
Kassapa, é a razão para afirmar: ‘Não existe outro mundo ... ’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. “Muito
bem então, Príncipe, eu explicarei com um símile, pois alguns sábios
compreendem o significado de um enunciado através de um símile. Certa vez,
Príncipe, certo Brâmane tinha duas esposas. Uma tinha um filho de dez ou doze
anos, enquanto que a outra estava grávida e próxima de dar à luz quando o
Brâmane morreu. Então, aquele jovem disse para a mulher grávida: ‘Senhora, toda
a riqueza e posses, prata ou ouro, que possa existir, será tudo meu. Meu pai
fez de mim o seu herdeiro. ’ Em resposta, a senhora Brâmane disse para o jovem:
‘Espere, jovem, até que eu dê à luz. Se o bebê for um menino, uma parte será
dele e se for uma menina, ela se tornará sua serva. ’ O jovem repetiu as suas
palavras uma segunda vez e recebeu a mesma resposta. Quando ele as repetiu pela
terceira vez, a senhora tomou uma faca e indo para um cômodo privado, cortou a
barriga, pensando: ‘Se pelo menos pudesse descobrir se é um menino ou menina!
’” E assim ela destruiu a si mesma e o feto, bem como a riqueza, tal como fazem
os tolos que buscam a sua herança sem sabedoria, desatentos aos perigos
ocultos. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Do mesmo
modo você, Príncipe, irá tolamente enfrentar perigos ocultos ao buscar um outro
mundo sem sabedoria, como aquela senhora Brâmane ao buscar a sua herança. Mas,
Príncipe, aqueles contemplativos e Brâmanes que observam a virtude e são bem
comportados não buscam acelerar o amadurecimento daquilo que ainda não está
maduro, mas ao invés disso, com sabedoria eles aguardam o seu amadurecimento. A
vida é benéfica para esses contemplativos e Brâmanes, pois quanto mais tempo
esses contemplativos e Brâmanes virtuosos e bem comportados permanecerem vivos,
mais méritos eles criarão; eles praticam para o bem-estar de muitos, para a
felicidade de muitos, por compaixão pelo mundo, pelo bem-estar e felicidade de
devas e humanos. Por conseguinte, Príncipe, admita que existe um outro mundo...
”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14. “Não
importa o que você diga com relação a isso, Venerável Kassapa, eu ainda penso
que não existe outro mundo ...” - “Você tem alguma outra justificativa para essa
afirmação, Príncipe?” - “Eu tenho, Venerável Kassapa.” - “Qual é, Príncipe?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
Kassapa, suponha que tragam à minha presença um ladrão pego em flagrante e
digam: ‘Este homem, Senhor, é um ladrão pego em flagrante. Sentencie-o a
qualquer punição que você desejar. ’ E eu diga: ‘Tomem esse homem e coloquem-no
vivo dentro de uma jarra. Fechem a boca da jarra com uma pele umedecida coberta
com uma grossa camada de argila úmida, coloquem-na num forno e acendam o fogo.
’ E assim eles fazem. Quando temos certeza que o homem está morto, removemos a
jarra, quebramos a argila, destampamos a boca e observamos com cuidado: ‘Talvez
possamos ver a alma [<a href="#N4">4</a>] <a name=R4></a>dele escapando. ’ Mas
nós não vemos nenhuma alma escapando. Essa, Venerável Kassapa, é a razão para
afirmar: ‘Não existe outro mundo... ’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. “Quanto
a isso, Príncipe, eu lhe farei uma pergunta em retorno. Responda como quiser.
Você admite que ao ir para a sua sesta ao meio-dia você teve visões prazerosas
de parques, florestas, campinas e lagos com flores de lótus?” - “Eu tive,
Venerável Kassapa.” - “E nessa ocasião você não é observado por corcundas, anões,
