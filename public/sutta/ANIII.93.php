<p class=Normal> “Há essas três tarefas urgentes de um agricultor. Quais três? </p>

<p class=Normal> “É o caso em que um agricultor com rapidez ara e grada bem o seu campo. Tendo rapidamente arado e gradado o seu campo, ele com rapidez planta as sementes. Tendo rapidamente plantado as sementes, ele com rapidez irriga o campo e escoa a água.  </p>

<p class=Normal> “Essas são as três tarefas urgentes de um agricultor. Agora, esse agricultor não tem o poder ou a força [para dizer:] ‘Que a minha plantação brote hoje, que os grãos apareçam amanhã, e que eles estejam maduros no dia seguinte.’ Mas no momento apropriado, a plantação irá brotar, os grãos irão aparecer e irão amadurecer. </p>

<p class=Normal> “Do mesmo modo, há essas três tarefas urgentes de um bhikkhu. Quais três? O desenvolvimento da virtude superior, o desenvolvimento da mente superior, o desenvolvimento da sabedoria superior. [<a href="#N1">1</a>] <a name="R1"></a>Essas são as três tarefas urgentes de um bhikkhu. Agora, esse bhikkhu não tem o poder ou a força [para dizer:] ‘Que a minha mente se liberte das impurezas através do desapego hoje, ou amanhã, ou depois de amanhã.’ Mas no momento apropriado, a sua mente é libertada das impurezas através do desapego. </p>

<p class=Normal> “Assim, bhikkhus, vocês deveriam treinar: ‘Ardente será o nosso desejo em desenvolver a virtude superior.  Ardente será o nosso desejo em desenvolver a mente superior. Ardente será o nosso desejo em desenvolver a sabedoria superior.’ Assim é como vocês deveriam treinar.” </p>

