<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no
Parque de Anathapindika. Lá ele se dirigiu aos monges desta forma: “Bhikkhus” –
“Venerável Senhor,” eles responderam. O Abençoado disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.
“Bhikkhus, a maioria dos seres possui este anseio, desejo e aspiração: ‘Se apenas
as coisas não desejadas, não cobiçadas, desagradáveis diminuíssem e aquelas
desejadas, cobiçadas e agradáveis aumentassem!’ No entanto, embora os seres
tenham esse anseio, desejo e aspiração, as coisas não desejadas, não cobiçadas,
desagradáveis aumentam para eles e as coisas desejadas, cobiçadas e agradáveis
diminuem para eles. Agora, bhikkhus, qual vocês pensam ser a razão disso?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, os nossos ensinamentos têm o Abençoado como origem, como guia e como
refúgio. Seria bom se o Abençoado pudesse explicar o significado dessas
palavras. Tendo ouvido do Abençoado, os bhikkhus o recordarão.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
bhikkhus, ouçam e prestem muita atenção àquilo que eu vou dizer.” – “Sim,
venerável senhor,” os bhikkhus responderam. O Abençoado disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. “Aqui,
bhikkhus, uma pessoa comum sem instrução que não respeita os nobres, que não é
proficiente nem treinada no Dhamma deles, que não respeita os homens
verdadeiros, que não é proficiente nem treinada no Dhamma deles, não sabe
quais coisas devem ser cultivadas e quais coisas não devem ser cultivadas, ela
não sabe quais coisas devem ser desenvolvidas e quais coisas não devem ser
desenvolvidas. Não sabendo isso, ela cultiva as coisas que não devem ser
cultivadas e não cultiva as coisas que devem ser cultivadas, ela desenvolve as
coisas que não devem ser desenvolvidas e não desenvolve as coisas que devem ser
desenvolvidas. [<a href="#N1">1</a>] <a name=R1></a>É porque ela faz isso que
as coisas não desejadas, não cobiçadas, desagradáveis aumentam para ela e as coisas
desejadas, cobiçadas e agradáveis diminuem para ela. Por que isso? Isso é o que acontece
com aquele que não vê.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. “O nobre
discípulo bem instruído, que respeita os nobres, que é proficiente e
treinado no Dhamma deles, que respeita os homens verdadeiros, que é
proficiente e treinado no Dhamma deles, sabe quais coisas devem ser
cultivadas e quais coisas não devem ser cultivadas, ele sabe quais coisas devem
ser desenvolvidas e quais coisas não devem ser desenvolvidas. Sabendo isso, ele
cultiva as coisas que devem ser cultivadas e não cultiva as coisas que não
devem ser cultivadas, ele desenvolve as coisas que devem ser desenvolvidas e
não desenvolve as coisas que não devem ser desenvolvidas. É porque ele faz isso
que as coisas desejadas, cobiçadas, agradáveis aumentam para ele e as coisas
não desejadas, não cobiçadas e desagradáveis diminuem para ele. Por que isso? Isso é o
que acontece com aquele que vê.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5.
“Bhikkhus, existem quatro maneiras de fazer as coisas. Quais quatro? Existe uma
maneira de fazer as coisas que é dolorosa agora e amadurece no futuro como
dolorosa. Existe uma maneira de fazer as coisas que é prazerosa agora e
amadurece no futuro como dolorosa. Existe uma maneira de fazer as coisas que é
dolorosa agora e amadurece no futuro como prazerosa. Existe uma maneira de
fazer as coisas que é prazerosa agora e amadurece no futuro como prazerosa.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(A PESSOA
IGNORANTE)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. (1)
“Agora, bhikkhus, aquele que é ignorante, sem saber esta maneira de fazer as coisas
que é dolorosa agora e amadurece no futuro como dolorosa, não compreende como
na verdade é, assim: ‘Esta maneira de fazer as coisas é dolorosa agora e
amadurece no futuro como dolorosa.’ Não sabendo isso, não compreendendo como na
verdade é, o ignorante cultiva isso e não evita isso; porque ele assim age, as
coisas não desejadas, não cobiçadas, desagradáveis aumentam para ele e as
coisas desejadas, cobiçadas e agradáveis diminuem para ele. Por que isso? Isso é o que
acontece com aquele que não vê.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. (2) “Agora,
bhikkhus, aquele que é ignorante, sem saber esta maneira de fazer as coisas que
é prazerosa agora e amadurece no futuro como dolorosa, não compreende como na
verdade é, assim: ‘Esta maneira de fazer as coisas é prazerosa agora e
amadurece no futuro como dolorosa.’ Não sabendo isso, não compreendendo como na
verdade é, o ignorante cultiva isso e não evita isso; porque ele assim age, as
coisas não desejadas ... aumentam para ele e as coisas desejadas ... diminuem para ele.
Por que isso? Isso é o que acontece com aquele que não vê.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. (3)
“Agora, bhikkhus, aquele que é ignorante, sem saber esta maneira de fazer as
coisas que é dolorosa agora e amadurece no futuro como prazerosa, não
compreende como na verdade é, assim: ‘Esta maneira de fazer as coisas é
dolorosa agora e amadurece no futuro como prazerosa.’ Não sabendo isso, não
compreendendo como na verdade é, o ignorante não cultiva isso mas evita isso;
porque ele assim age, as coisas não desejadas ... aumentam para ele e as coisas
desejadas ... diminuem para ele. Por que isso? Isso é o que acontece com aquele que não
vê.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. (4)
“Agora, bhikkhus, aquele que é ignorante, sem saber esta maneira de fazer as
coisas que é prazerosa agora e amadurece no futuro como prazerosa, não
compreende como na verdade é, assim: ‘Esta maneira de fazer as coisas é
prazerosa agora e amadurece no futuro como prazerosa.’ Não sabendo isso, não
compreendendo como na verdade é, o ignorante não cultiva isso mas evita isso;
porque ele assim age, as coisas não desejadas ... aumentam para ele e as coisas
desejadas ... diminuem para ele. Por que isso? Isso é o que acontece com aquele que não
vê.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(A PESSOA
SÁBIA)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. (1)
“Agora, bhikkhus, aquele que é sábio, sabendo esta maneira de fazer as coisas
que é dolorosa agora e amadurece no futuro como dolorosa, compreende como na
verdade é, assim: ‘Esta maneira de fazer as coisas é dolorosa agora e amadurece
no futuro como dolorosa.’ Sabendo isso, compreendendo como na verdade é, o
sábio não cultiva isso mas evita isso; porque ele assim age, as coisas não
desejadas, não cobiçadas, desagradáveis diminuem para ele e as coisas
desejadas, cobiçadas e agradáveis aumentam para ele. Por que isso? Isso é o que acontece
com aquele que vê.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. (2)
“Agora, bhikkhus, aquele que é sábio, sabendo esta maneira de fazer as coisas
que é prazerosa agora e amadurece no futuro como dolorosa, compreende como na
verdade é, assim: ‘Esta maneira de fazer as coisas é prazerosa agora e
amadurece no futuro como dolorosa.’ Sabendo isso, compreendendo como na verdade
é, o sábio não cultiva isso mas evita isso; porque ele assim age, as coisas não
desejadas ... diminuem para ele e as coisas desejadas ... aumentam para ele. Por que isso? Isso é o que acontece com aquele que vê.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. (3)
“Agora, bhikkhus, aquele que é sábio, sabendo esta maneira de fazer as coisas
que é dolorosa agora e amadurece no futuro como prazerosa, compreende como na
verdade é, assim: ‘Esta maneira de fazer as coisas é dolorosa agora e amadurece
no futuro como prazerosa.’ Sabendo isso, compreendendo como na verdade é, o
sábio não evita isso mas cultiva isso; porque ele assim age, as coisas não
desejadas ... diminuem para ele e as coisas desejadas ... aumentam para ele. Por que isso? Isso é o que acontece com aquele que vê.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. (4)
“Agora, bhikkhus, aquele que é sábio, sabendo esta maneira de fazer as coisas
que é prazerosa agora e amadurece no futuro como prazerosa, compreende como na
verdade é, assim: ‘Esta maneira de fazer as coisas é prazerosa agora e
amadurece no futuro como prazerosa.’ Sabendo isso, compreendendo como na
verdade é, o sábio não evita isso mas cultiva isso; porque ele assim age, as
coisas não desejadas ... diminuem para ele e as coisas desejadas ... aumentam para ele.
Por que isso? Isso é o que acontece com aquele que vê. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(AS QUATRO
MANEIRAS)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14. (1)
“Qual, bhikkhus, é a maneira de fazer as coisas que é<span
style='mso-spacerun:yes'>  </span>dolorosa agora e amadurece no futuro como
dolorosa? Aqui, bhikkhus, alguém com dor e tristeza mata seres vivos, ele
experimenta a dor e a tristeza que têm o ato de matar seres vivos como
condição. Com dor e tristeza ele toma aquilo que não é dado ... se comporta de
forma imprópria em relação aos prazeres sensuais ... fala mentiras ... fala com
malícia ... fala de forma grosseira ... fala frivolidades ... é cobiçoso ...
tem má vontade na mente ... tem o entendimento incorreto, e ele experimenta a
dor e a tristeza que têm o entendimento incorreto como condição. Com a
dissolução do corpo, após a morte, ele renasce num estado de privação, num
destino infeliz, nos reinos inferiores e até mesmo no <span style='mso-bidi-font-weight:
bold'>inferno. Essa é </span>a maneira de fazer as coisas que é dolorosa agora
e que amadurece no futuro como dolorosa.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. (2)
“Qual, bhikkhus, é a maneira de fazer as coisas que é prazerosa agora e
amadurece no futuro como dolorosa? Aqui, bhikkhus, alguém com prazer e alegria
mata seres vivos, ele experimenta o prazer e a alegria que têm o ato de matar
seres vivos como condição. Com prazer e alegria ele toma aquilo que não é dado
... tem o entendimento incorreto, e ele experimenta o prazer e a alegria que
têm o entendimento incorreto como condição. Com a dissolução do corpo, após a morte,
ele renasce num estado de privação, num destino infeliz, nos reinos inferiores,
até mesmo no <span style='mso-bidi-font-weight:bold'>inferno</span>. Essa é a
maneira de fazer as coisas que é prazerosa agora e que amadurece no futuro como
dolorosa.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16. (3)
“Qual, bhikkhus, é a maneira de fazer as coisas que é dolorosa agora e
amadurece no futuro como prazerosa? Aqui, bhikkhus, alguém com dor e tristeza
se abstém de matar seres vivos, ele experimenta a dor e a tristeza que têm a
abstenção de matar seres vivos como condição. Com dor e tristeza ele não toma
aquilo que não é dado ... não se comporta de forma imprópria em relação aos
prazeres sensuais ... não fala mentiras ... não fala com malícia ... não fala
de forma grosseira ... não fala frivolidades ... não é cobiçoso ... não tem má
vontade na mente... tem o entendimento correto, e ele<span
style='mso-spacerun:yes'>  </span>experimenta a dor e a tristeza que têm o
entendimento correto como condição. Com a dissolução do corpo, após a morte,
ele renasce num destino feliz e até mesmo no paraíso. Essa é a maneira de fazer
as coisas que é dolorosa agora e que amadurece no futuro como prazerosa.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR>17. (4) “Qual, bhikkhus, é a maneira de
fazer as coisas que é prazerosa agora e amadurece no futuro como prazerosa?
Aqui, bhikkhus, alguém com prazer e alegria se abstém de matar seres vivos, ele
experimenta o prazer e a alegria que têm a abstenção de matar seres vivos como
condição. Com prazer e alegria ele não toma aquilo que não é dado ... tem o
entendimento correto, e ele experimenta o prazer e a alegria que têm o
entendimento correto como condição. Com a dissolução do corpo, após a morte,
ele renasce num destino feliz e até mesmo no paraíso. Essa é a maneira de fazer
as coisas que é prazerosa agora e que amadurece no futuro como prazerosa.</span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(OS
SÍMILES)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18. (1) “Bhikkhus,
suponham que houvesse uma abóbora amarga misturada com veneno e viesse um homem
que deseja viver não morrer, que deseja o prazer e evita a dor, e dissessem
para ele: ‘Bom homem, esta abóbora amarga está misturada com veneno. Coma se
quiser; ao comê-la, a cor, o cheiro e o sabor não lhe cairão bem e depois de
comê-la você morrerá ou terá um sofrimento igual à morte.’ Então, ele come sem
refletir e não a coloca de lado. Ao comê-la, a cor, o cheiro e o sabor dela não
lhe caem bem e depois de comê-la ele morre ou tem um sofrimento igual à morte.
Semelhante a isso, eu digo, é a maneira de fazer as coisas que é<span
style='mso-spacerun:yes'>  </span>dolorosa agora e amadurece no futuro como
dolorosa.<o:p></o:p></span></p>



<p class=Normal><a name=T19></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>19. (2) “Suponham que houvesse uma taça de bronze com uma bebida com boa
cor, cheiro e sabor, mas estivesse misturada com veneno e viesse um homem<span
style='mso-spacerun:yes'>  </span>que deseja viver não morrer, que deseja o
prazer e evita a dor, e dissessem para ele: Bom homem, esta taça de bronze
contém uma bebida com boa cor, cheiro e sabor, mas está misturada com veneno.
Beba se quiser; ao bebê-la, a cor, o cheiro e o sabor lhe cairão bem, mas
depois de bebê-la você morrerá ou terá um sofrimento igual à morte.’ Então ele bebe
sem refletir e não a coloca de lado. Ao bebê-la, a cor, o cheiro e o sabor lhe
caem bem, mas depois de bebê-la ele morre ou tem um sofrimento igual à morte.
Semelhante a isso, eu digo, é a maneira de fazer as coisas que é prazerosa
agora e que amadurece no futuro como dolorosa.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>20. (3)
“Suponham que houvesse urina fermentada misturada com vários medicamentos e
viesse um homem enfermo com icterícia e dissessem para ele: ‘Bom homem, esta
urina fermentada está misturada com vários medicamentos. Beba se quiser; ao
bebê-la, a cor, o cheiro e o sabor não lhe cairão bem, mas depois de bebê-la
você ficará melhor.’ Então ele bebe depois de refletir e não a coloca de lado.
Ao bebê-la, a cor, o cheiro e o sabor<span style='mso-spacerun:yes'> 
</span>não lhe caem bem, mas depois de bebê-la ele melhora. Semelhante a isso,
eu digo, é a maneira de fazer as coisas que é dolorosa agora e amadurece no
futuro como prazerosa.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>21. (4)
“Suponham que houvesse coalhada, mel, manteiga líquida clarificada e melaço
misturados juntos, e viesse um homem com disenteria e dissessem para ele: ‘Bom
homem, isto é coalhada, mel, manteiga líquida clarificada e melaço misturados
juntos. Beba se quiser; ao bebê-la, a cor, o cheiro e o sabor lhe cairão bem e
depois de bebê-la você ficará melhor.’ Então ele bebe depois de refletir e não
a coloca de lado. Ao bebê-la, a cor, o cheiro, e o sabor lhe caem bem e depois
de bebê-la ele melhora. Semelhante a isso, eu digo, é a maneira de fazer as
coisas que é prazerosa agora e amadurece no futuro como prazerosa.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>22. “Tal
como no outono, no último mês da estação das chuvas, quando o céu está limpo e
sem nuvens, o sol surge na terra dissipando toda a escuridão do espaço com a
sua luminosidade brilhante e radiante, assim também, a maneira de fazer as
coisas, que é prazerosa agora e amadurece no futuro como prazerosa, dissipa com
o seu brilho e luminosidade e radiância todas as demais doutrinas de quaisquer
contemplativos e brâmanes.” <o:p></o:p></span></p>



