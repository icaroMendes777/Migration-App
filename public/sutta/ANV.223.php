<p class=Normal> "Bhikkhus, há esses cinco perigos em residir por muito tempo no mesmo lugar. Quais cinco? (1) Alguém passa a ter a posse e acumular muitas coisas. (2) Ele passa a ter a posse e acumular muitos medicamentos. (3) Ele assume muitas tarefas e obrigações e se torna competente nas várias coisas por fazer. (4) Ele estabelece vínculos inadequados com chefes de família e monásticos de um modo típico das pessoas leigas. (5) Quando parte daquele monastério ele parte cheio de preocupações. Esses são os cinco perigos em residir por muito tempo no mesmo lugar.</p>

<p class=Normal> "Bhikkhus, há esses cinco benefícios em residir por um tempo equilibrado no mesmo lugar. Quais cinco? (1) Alguém não passa a ter a posse e acumular muitas coisas. (2) Ele não passa a ter a posse e acumular muitos medicamentos. (3) Ele não assume muitas tarefas e obrigações e se torna competente nas várias coisas por fazer. (4) Ele não estabelece vínculos inadequados com chefes de família e monásticos de um modo típico das pessoas leigas. (5) Quando parte daquele monastério ele parte sem preocupações. Esses são os cinco benefícios em residir por um tempo equilibrado no mesmo lugar."
