<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim ouvi.
Em certa ocasião o Abençoado estava<span style='mso-spacerun:yes'>  </span>em
Savatthi no Bosque de Jeta, no Parque de Anathapindika. Então, quando a noite
estava bem avançada, uma certa devata com belíssima aparência que iluminou todo
o Bosque de Jeta, se aproximou do Abençoado. Ao se aproximar ela homenageou o
Abençoado e ficando em pé a um lado a devata disse:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Nós lhe
perguntamos Gotama, sobre o declínio do homem. Qual é a causa para a ruína?” <o:p></o:p></span></p>



<p class=Normal><i><span lang=PT-BR style='mso-ansi-language:PT-BR'>O Buda</span></i><span
lang=PT-BR style='mso-ansi-language:PT-BR'>:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“É fácil de
distinguir aquele que progride, fácil de distinguir aquele que declina. Aquele
que ama o Dhamma progride; aquele que é adverso ao Dhamma, declina.” <o:p></o:p></span></p>



<p class=Normal><i><span lang=PT-BR style='mso-ansi-language:PT-BR'>A devata</span></i><span
lang=PT-BR style='mso-ansi-language:PT-BR'>:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Nós
entendemos isso tal como foi explicado; essa é a primeira causa para a ruína.
Diga, Abençoado, qual é a segunda causa para a ruína?” [<a href="#N1">1</a>] <o:p></o:p></span></p>



<p class=Normal><a name=R1></a><i><span lang=PT-BR style='mso-ansi-language:
PT-BR'>O Buda</span></i><span lang=PT-BR style='mso-ansi-language:PT-BR'>:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Os
depravados são amados por ele, ele não gosta dos virtuosos; ele aprova os ensinamentos
dos depravados – essa é uma causa para a sua ruína. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele gosta
de dormir, aprecia as companhias, indolente, preguiçoso e irritadiço – essa é
uma causa para a sua ruína.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Embora
sendo opulento, ele não sustenta pai e mãe que são idosos e deixaram para trás
o vigor da juventude – essa é uma causa para a sua ruína.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele engana
com a mentira um brâmane ou contemplativo ou qualquer outro mendicante – essa é
uma causa para a sua ruína.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele tem
muita riqueza, muito ouro e comida, mas desfruta disso tudo sozinho – essa é
uma causa para a sua ruína.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele tem
orgulho do seu nascimento, da sua riqueza ou seu clã, e despreza os próprios
parentes – essa é uma causa para a sua ruína.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele é um
devasso, um bêbado, um jogador, que desperdiça tudo que ganha – essa é uma
causa para a sua ruína.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele não
está satisfeito com as esposas que tem, [<a href="#N2">2</a>] <a name=R2></a>e
ele é visto entre prostitutas e as mulheres dos outros – essa é uma causa para
a sua ruína.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Tendo
deixado para trás a juventude, ele toma como esposa uma jovem adolescente e é
incapaz de dormir devido ao ciúme – essa é uma causa para a sua ruína.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele coloca
numa posição de autoridade uma mulher que tem o hábito da bebida e do
desperdício, um homem com comportamento semelhante – essa é uma causa para a
sua ruína.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele nasce
um nobre, com muita ambição mas parcos recursos, e ambiciona governar – essa é
uma causa para a sua ruína.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Compreendendo
bem essas causas para a ruína no mundo, o nobre sábio dotado com o insight,
desfruta da bem-aventurança. <o:p></o:p></span></p>

