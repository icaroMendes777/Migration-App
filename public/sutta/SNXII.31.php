<p class=Normal><a name=N2></a> [2] <i>Bhutamidanti, bhante, yathabhutam sammappaññaya passati</i>: <i>yatha bhuta</i> como regra geral tanto no português como no inglês tem sido traduzido “como as coisas na verdade são”. Essa tradução no entanto apresenta algumas limitações no sentido de dar a impressão que existe algo especial a ser visto “nas coisas” e que a experiência de insight corresponde a ver esse algo especial, quando na verdade estamos mais interessados em ver o processo de fabricação que ocorre na mente e nesse sentido uma tradução mais esclarecedora para <i>yatha butha</i> seria “como as coisas vêm a ser” (através do processo de fabricação na mente). Essa foi a tradução empregada neste sutta. [<a href="#R2">Retorna</a>] </p>

