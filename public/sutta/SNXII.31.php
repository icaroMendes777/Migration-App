<p class=Normal> Certa ocasião o Abençoado estava em Savathi, lá ele se dirigiu ao venerável Sariputta assim: </p>

<p class=Normal>"Sariputta, nas Perguntas de Ajita no Parayana é dito: [<a href="#N1">1</a>]</p>

<p class=Normal><a name=R1></a>'Aqueles que compreenderam completamente o Dhamma, aqueles em treinamento e os outros indivíduos aqui,  explique-me como eles se comportam.'</p>

<p class=Normal>Como deve ser esse breve enunciado ser entendido em detalhe?" </p>

<p class=Normal>Quando isso foi dito o venerável Sariputta permaneceu em silêncio. Uma segunda vez e uma terceira vez o Abençoado se dirigiu ao venerável Sariputta assim: "Sariputta, nas Perguntas de Ajita no Parayana ... Como deve ser esse breve enunciado ser entendido em detalhe?" Uma segunda e uma terceira vez o venerável Sariputta permaneceu em silêncio. </p>

<p class=Normal>"Sariputta, você vê: 'Isto veio a ser'? Sariputta, você vê: 'Isto veio a ser'?" </p>

<p class=Normal><a name=R2></a>"Venerável senhor, alguém vê com correta sabedoria como na verdade veio a ser [<a href="#N2">2</a>]: 'Isto veio a ser'. Tendo visto com correta sabedoria: 'Isto veio a ser', ele pratica para o desencantamento daquilo que veio a ser, para o desapego, e para a cessação. Ele vê com correta sabedoria: 'A sua originação ocorre com aquilo como alimento.' Tendo visto com correta sabedoria: 'A sua originação ocorre com aquilo como alimento', ele pratica para o desencantamento da sua originação daquilo como alimento, para o desapego, e para a cessação. Ele vê com correta sabedoria: 'Com a cessação daquele alimento, aquilo que veio a ser está sujeito à cessação.' Tendo visto com correta sabedoria: 'Com a cessação daquele alimento, aquilo que veio a ser está sujeito à cessação', ele pratica para o desencantamento daquilo que está sujeito à cessação, para o desapego, e para a cessação. [<a href="#N3">3</a>]</p>

<p class=Normal><a name=R3></a>"Como, venerável senhor, alguém compreendeu completamente o Dhamma? Venerável senhor, alguém vê com correta sabedoria como na verdade veio a ser: 'Isto veio a ser'. Tendo visto com correta sabedoria: 'Isto veio a ser', através do desencantamento com relação ao que veio a ser, através do desapego e cessação, ele está libertado através do desapego. Ele vê com correta sabedoria: 'A sua originação ocorre com aquilo como alimento.' Tendo visto com correta sabedoria: 'A sua originação ocorre com aquilo como alimento', através do desencantamento com a originação através daquilo como alimento, através do desapego e cessação, ele está libertado através do desapego. Ele vê como na verdade veio a ser com correta sabedoria: 'Com a cessação daquele alimento, aquilo que veio a ser está sujeito à cessação.' Tendo visto com correta sabedoria: 'Com a cessação daquele alimento, aquilo que veio a ser está sujeito à cessação', através do desencantamento com aquilo que está sujeito à cessação, através do desapego e cessação, ele está libertado através do desapego. Desse modo alguém compreendeu o Dhamma. </p>

<p class=Normal>"Venerável senhor, portanto quando foi dito nas Perguntas de Ajita no Parayana: </p>

<p class=Normal>'Aqueles que compreenderam completamente o Dhamma, aqueles em treinamento e os outros indivíduos aqui,  explique-me como eles se comportam.'</p>

<p class=Normal>é dessa maneira que compreendo em detalhe esse breve enunciado." </p>

<p class=Normal>"Muito bem, muito bem Sariputta! ... (o Buda repete tudo que foi dito pelo venerável Sariputta) ... é dessa maneira que deve ser compreendido em detalhe esse breve enunciado."</p>

