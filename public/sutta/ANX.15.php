<p class=Normal> “Entre todos os seres que possam existir – sem patas, com duas patas, quatro patas, muitas patas; com forma ou sem forma; perceptivos, não perceptivos, nem perceptivos nem não perceptivos – o Tathagata, um arahant, perfeitamente iluminado, é considerado supremo; do mesmo modo, todas as qualidades hábeis estão enraizadas na diligência, convergem na diligência e a diligência é considerada como a melhor entre todas elas. </p>

<p class=Normal> “Tal como a pegada de qualquer ser vivo que caminha pode ser colocada dentro da pegada de um elefante e assim a pegada do elefante é declarada como a líder delas devido ao seu grande tamanho; do mesmo modo, todas as qualidades hábeis estão enraizadas na diligência, convergem na diligência e a diligência é considerada como a melhor entre todas elas. </p>

<p class=Normal> “Tal como os caibros de uma casa com cumeeira se dirigem todos para a cumeeira, se inclinam para a cumeeira, convergem na cumeeira e a cumeeira é considerada como a melhor entre todos eles; do mesmo modo, todas as qualidades hábeis estão enraizadas na diligência, convergem na diligência e a diligência é considerada como a melhor entre todas elas. </p>

<p class=Normal> “Tal como entre todas as raízes perfumadas, a raiz da íris negra é considerada como contendo o melhor perfume entre as raízes; do mesmo modo, todas as qualidades hábeis estão enraizadas na diligência, convergem na diligência e a diligência é considerada como a melhor entre todas elas. </p>

<p class=Normal> “Tal como entre todas as madeiras perfumadas, o sândalo vermelho é considerado como o melhor perfume entre as madeiras; do mesmo modo, todas as qualidades hábeis estão enraizadas na diligência, convergem na diligência e a diligência é considerada como a melhor entre todas elas. </p>

<p class=Normal> “Tal como entre todas as flores perfumadas, o jasmim é considerado o melhor perfume entre as flores; do mesmo modo, todas as qualidades hábeis estão enraizadas na diligência, convergem na diligência e a diligência é considerada como a melhor entre todas elas. </p>

<p class=Normal> “Tal como todos os príncipes menores se tornam vassalos de um monarca que gira a roda e o monarca que gira a roda é considerado como o melhor entre todos eles; do mesmo modo, todas as qualidades hábeis estão enraizadas na diligência, convergem na diligência e a diligência é considerada como a melhor entre todas elas. </p>

<p class=Normal> “Tal como a luminosidade das constelações não equivale a um dezesseis avos da luminosidade da lua, e a luminosidade da lua é considerada como a melhor entre todas elas; do mesmo modo, todas as qualidades hábeis estão enraizadas na diligência, convergem na diligência e a diligência é considerada como a melhor entre todas elas. </p>

<p class=Normal> “Tal
como no outono, no último mês da estação das chuvas, quando o céu está limpo e
sem nuvens, o sol surge na terra dissipando toda a escuridão do espaço com a
sua luminosidade brilhante e radiante; do mesmo modo, todas as qualidades hábeis estão enraizadas na diligência, convergem na diligência e a diligência é considerada como a melhor entre todas elas. </p>

<p class=Normal> “Tal como os grandes rios que existem – o Gânges, o Yamuna, o Aciravati, o Sarabhu e o Mahi — todos vão para o oceano, declivam para o oceano, tendem para o oceano, se inclinam para o oceano, e o oceano é considerado como o melhor entre eles; do mesmo modo, todas as qualidades hábeis estão enraizadas na diligência, convergem na diligência e a diligência é considerada como a melhor entre todas elas.” </p>

