<p class=Normal> 
Essas quatro tradições dos Nobres – originais, muito antigas,
tradicionais, não adulteradas, íntegras desde o princípio – não são suscetíveis
de suspeita, nunca estarão sujeitas à suspeita, não são criticadas pelos sábios,
brâmanes e contemplativos. Quais quatro?<span style="mso-spacerun: yes"> 
</span><o:p></o:p></span></p>



<p class=Normal>
É o caso em que o bhikkhu está satisfeito com qualquer tecido para um
manto. Ele louva a satisfação com qualquer tecido para um manto. Por causa do
tecido para um manto ele não faz nada que seja inadequado ou não apropriado.
Não obtendo o tecido ele não fica agitado. Obtendo o tecido, ele o utiliza sem
gerar apego, desencantado, sem culpa, vendo<span style="mso-spacerun: yes"> 
</span>as desvantagens (resultantes do apego ), e discernindo como escapar
dele. Por conta de sua satisfação com qualquer tecido para um manto, ele não
exalta a si mesmo e nem menospreza os outros. Nisso ele é habilidoso,
energético, com plena consciência e plenamente atento. Dessa forma, bhikkhus, se caracteriza
um bhikkhu que se mantém firme nas antigas e originais tradições dos Nobres.<o:p></o:p></span></p>



<p class=Normal>
Além disso, o bhikkhu está satisfeito com qualquer alimento esmolado.
Ele louva a satisfação com qualquer alimento esmolado. Por causa do alimento
ele não faz nada que seja inadequado ou não apropriado. Não obtendo alimento
esmolado, ele não fica agitado. Obtendo alimento esmolado, ele o utiliza sem
gerar apego, desencantado, sem culpa, vendo as desvantagens (resultantes do
apego ), e discernindo como escapar dele. Por conta de sua satisfação com o
alimento esmolado, ele não exalta a si mesmo e nem menospreza os outros. Nisso
ele é habilidoso, energético, com plena consciência e plenamente atento. Dessa forma,
bhikkhus, se caracteriza um bhikkhu que se mantém firme nas antigas e originais
tradições dos Nobres.<o:p></o:p></span></p>



<p class=Normal>
Além disso, o bhikkhu está satisfeito com qualquer moradia. Ele louva a
satisfação com qualquer moradia. Por causa de uma moradia ele não faz nada que
seja inadequado ou não apropriado. Não obtendo uma moradia, ele não fica
agitado. Obtendo uma moradia, ele a utiliza sem gerar apego, desencantado, sem
culpa, vendo as desvantagens (resultantes do apego ), e discernindo como
escapar dele. Por conta de sua satisfação com a moradia, ele não exalta a si
mesmo e nem menospreza os outros. Nisso ele é habilidoso, energético, com plena consciência e
plenamente atento. Dessa forma, bhikkhus, se caracteriza um bhikkhu que se
mantém firme nas antigas e originais tradições dos Nobres.<o:p></o:p></span></p>



<p class=Normal>
Além disso, o bhikkhu encontra prazer e deleite na meditação, encontra prazer e deleite no abandono. Por conta de seu prazer e deleite na meditação e no abandono, ele não exalta a si mesmo e nem menospreza os
outros. Nisso ele é habilidoso, energético, com plena consciência e plenamente atento. Dessa
forma, bhikkhus, se caracteriza um bhikkhu que se mantém firme nas antigas e
originais tradições dos Nobres.<o:p></o:p></span></p>



<p class=Normal>
Essas são as quatro tradições dos Nobres – originais, muito antigas,
tradicionais, não adulteradas, íntegras desde o princípio – não são suscetíveis
de suspeita, nunca estarão sujeitas à suspeita, não são criticadas pelos sábios,
brâmanes e contemplativos.<o:p></o:p></span></p>



<p class=Normal>
Além disso, um bhikkhu dotado dessas quatro tradições dos Nobres, se ele
viver no leste, ele conquistará o desprazer e não será conquistado pelo
desprazer. Se ele viver no oeste ... no norte ... no sul, ele conquistará o
desprazer e não será conquistado pelo desprazer. Por que isso? Porque os sábios toleram
ambos o prazer e o desprazer.<o:p></o:p></span></p>


<p class=Normal>
Isso foi o que o Abençoado disse. Tendo dito isto, ele disse mais:<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>O desprazer não conquista o iluminado.<br>

O desprazer não o
abate.<br>

Ele conquista o
desprazer <br>

porque ele o tolera. <o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>Tendo descartado todas ações:<br>

quem poderá
obstruí-lo?<br>

Como um ornamento
feito com o ouro mais puro:<br>

Quem encontrará nele
algum defeito?<br>

Mesmo os Devas o
elogiam,<br>

até mesmo Brahma o elogia.<o:p></o:p></span></p>

