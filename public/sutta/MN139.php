<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim ouvi.
Em certa ocasião o Abençoado estava em Savatthi, no Bosque de Jeta, no Parque de
Anathapindika. Lá ele se dirigiu aos monges desta forma: &quot;Bhikkhus.&quot;
– &quot;Venerável Senhor,&quot; eles responderam. O Abençoado disse o seguinte:<o:p></o:p></span></p>


<p class=Normal><a name=T2></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.
“Bhikkhus, eu ensinarei para vocês a análise do não conflito. Ouçam e prestem
muita atenção ao que vou dizer.” – “Sim, venerável senhor,” os bhikkhus
responderam. O Abençoado disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. A pessoa
não deve buscar os prazeres sensuais que são baixos, vulgares, grosseiros,
ignóbeis e que não trazem benefício; e não deve buscar a mortificação que é
dolorosa, ignóbil e que não traz benefício. O Caminho do Meio descoberto pelo
Tathagata evita ambos os extremos; proporcionando visão, proporcionando
conhecimento, conduzindo à paz, ao conhecimento direto, à iluminação, a
Nibbana. [<a href="#N1">1</a>] <a name=R1></a>A pessoa deve saber o que deve
ser enaltecido e o que deve ser depreciado e sabendo isso, não deve enaltecer
nem depreciar mas deve apenas ensinar o Dhamma. A pessoa deve saber como
definir o prazer e sabendo isso, deve buscar o prazer dentro de si. A pessoa
não deve falar com a linguagem dissimulada, e não deve falar com a linguagem
não dissimulada, severa. A pessoa deve falar sem pressa, não com pressa.<span
style='mso-spacerun:yes'>  </span>A pessoa não deve insistir no idioma local, e
não deve anular o uso popular. Esse é o sumário da análise do não conflito.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. “’A
pessoa não deve buscar os prazeres sensuais que são baixos, vulgares,
grosseiros, ignóbeis e que não trazem benefício; e a pessoa não deve buscar
a mortificação que é dolorosa, ignóbil e que não traz benefício.’ Assim foi
dito. E com referência a que foi dito isso? <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“A
busca do prazer, cuja satisfação está vinculada aos desejos sensuais [<a
href="#N2">2</a>] - <a name=R2></a>baixos, vulgares, grosseiros, ignóbeis e que
não trazem benefício – é um estado atormentado pelo sofrimento, aflição,
desespero e febre e é o caminho errado. [<a href="#N3">3</a>] <a name=R3></a>Desengajar-se
da busca do prazer, cuja satisfação está vinculada aos desejos sensuais -
baixos, vulgares, grosseiros, ignóbeis e que não trazem benefício – é um estado
sem sofrimento, aflição, desespero e febre e é o caminho correto.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“A
busca da mortificação - dolorosa, ignóbil e que não traz benefício - é um
estado atormentado pelo sofrimento, aflição, desespero e febre e é o caminho
errado. Desengajar-se da busca da mortificação - dolorosa, ignóbil e que
não traz benefício – é um estado sem sofrimento, aflição, desespero e febre e é
o caminho correto.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR
style='mso-ansi-language:PT-BR'>“Portanto, foi com referência a isso que foi
dito: ’A pessoa não deve buscar os prazeres sensuais que são baixos,
vulgares, grosseiros, ignóbeis e que não trazem benefício; e a pessoa não deve
buscar a mortificação que é dolorosa, ignóbil e que não traz benefício.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. “’O
Caminho do Meio descoberto pelo Tathagata evita ambos os extremos;
proporcionando visão, proporcionando conhecimento, conduzindo à paz, ao
conhecimento direto, à iluminação, a Nibbana.’ Assim foi dito. E com referência
a que foi dito isso? É exatamente este Nobre Caminho Óctuplo; isto é,
entendimento correto, pensamento correto, linguagem correta, ação correta, modo
de vida correto, esforço correto, atenção plena correta, concentração correta.
Portanto, foi com referência a isso que foi dito: ’O Caminho do Meio descoberto
pelo Tathagata evita ambos os extremos ... Nibbana.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. “’A
pessoa deve saber o que deve ser enaltecido e o que deve ser depreciado e
sabendo ambos, nao deve enaltecer nem depreciar mas deve apenas ensinar o
Dhamma.’ Assim foi dito. E com referência a que foi dito isso? <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. “Como,
bhikkhus, ocorre o enaltecimento, a depreciação e o fracasso em ensinar apenas
o Dhamma? Quando a pessoa diz: ‘Todos aqueles que estão buscando o prazer, cuja satisfação está vinculada aos desejos sensuais<span
style='mso-spacerun:yes'>  </span>– baixos ... que não trazem benefício - estão
atormentados pelo sofrimento, aflição, desespero e febre e entraram pelo
caminho errado,’<span style='mso-spacerun:yes'>  </span>a pessoa estará
depreciando alguém. Quando a pessoa diz: ‘Todos aqueles que não estão buscando o prazer, cuja satisfação está vinculada aos desejos
sensuais<span style='mso-spacerun:yes'>  </span>– baixos ...<span
style='mso-spacerun:yes'>  </span>que não trazem benefício – não estão
atormentados pelo sofrimento, aflição, desespero e febre e entraram pelo
caminho correto,’<span style='mso-spacerun:yes'>  </span>a pessoa estará
enaltecendo alguém.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando a
pessoa diz: ‘Todos aqueles que estão buscando a mortificação -
dolorosa, ignóbil e que não traz benefício - estão atormentados pelo
sofrimento, aflição, desespero e febre e entraram pelo caminho errado,’<span
style='mso-spacerun:yes'>  </span>a pessoa estará depreciando alguém. Quando a
pessoa diz: ‘Todos aqueles que não estão buscando a
mortificação - dolorosa, ignóbil e que não traz benefício - não estão
atormentados pelo sofrimento, aflição, desespero e febre e entraram pelo
caminho correto,’<span style='mso-spacerun:yes'>  </span>a pessoa estará
enaltecendo alguém.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando a
pessoa diz: ‘Todos aqueles que não abandonaram o grilhão de ser/existir [<a
href="#N4">4</a>] <a name=R4></a>estão atormentados pelo sofrimento, aflição,
desespero e febre e entraram pelo caminho errado,’ a pessoa estará depreciando
alguém. Quando a pessoa diz: ‘Todos aqueles que abandonaram o grilhão de
ser/existir - não estão atormentados pelo sofrimento, aflição, desespero e
febre e entraram pelo caminho correto,’ a pessoa estará enaltecendo alguém.
Assim é como ocorre o enaltecimento, a depreciação e o fracasso em ensinar
apenas o Dhamma.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. “E como,
bhikkhus, não ocorre nem o enaltecimento, nem a depreciação mas apenas o ensino
do Dhamma? Quando a pessoa não diz: ‘Todos aqueles que estão buscando o prazer, cuja satisfação está vinculada aos desejos sensuais ... entraram pelo
caminho errado,’ mas ao invés disso diz: ‘A busca é um estado atormentado pelo
sofrimento, aflição, desespero e febre e é o caminho errado,’ então, ela só
ensina o Dhamma. [<a href="#N5">5</a>] <a name=R5></a>Quando a pessoa não diz:
‘Todos aqueles que não estão buscando a mortificação ...
entraram pelo caminho correto,’ mas ao invés disso diz: ‘Não buscar é
um estado não atormentado pelo sofrimento, aflição, desespero e febre e é o
caminho certo,’ então ela só ensina o Dhamma. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando a
pessoa não diz: ‘Todos aqueles que não abandonaram o grilhão de ser/existir ...
entraram pelo caminho errado,’ mas ao invés disso diz: ‘Enquanto o grilhão de
ser/existir não for abandonado, o ser/existir também não será abandonado,’
então ela só ensina o Dhamma. Quando a pessoa não diz: ‘Todos aqueles que
abandonaram o grilhão de ser/existir ... entraram pelo caminho correto,’<span
style='mso-spacerun:yes'>  </span>mas ao invés disso diz: ‘Quando o grilhão de
ser/existir for abandonado, o ser/existir também será abandonado,’ então ela só
ensina o Dhamma. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-spacerun:yes'> </span>“Portanto, foi com referência a isso que foi
dito: ’A pessoa deve saber o que deve ser enaltecido e o que deve ser
depreciado e sabendo isso, não deve enaltecer nem depreciar, mas apenas ensinar
o Dhamma.’<o:p></o:p></span></p>



<p class=Normal><a name=T9></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>9. “’A pessoa deve saber como definir o prazer e sabendo isso, deve
buscar o prazer dentro de si.’ Assim foi dito. E com referência a que foi dito
isso? <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
existem esses cinco elementos do prazer sensual. Quais cinco? Formas percebidas
pelo olho ... sons percebidos pelo ouvido ... aromas percebidos pelo nariz ...
sabores percebidos pela língua ... tangíveis percebidos pelo corpo que são
desejáveis, agradáveis e fáceis de serem gostados, conectados com o desejo
sensual e que provocam a cobiça. Esses são os cinco elementos do prazer
sensual. Agora o prazer e a alegria que surgem na dependência desses cinco
elementos do prazer sensual é chamado de prazer sensual – um prazer baixo, um
prazer grosseiro, um prazer ignóbil. Eu digo desse tipo de prazer que ele não
deve ser perseguido, ele não deve ser desenvolvido, ele não deve ser cultivado,
ele deve ser temido.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
bhikkhus, um bhikkhu afastado dos prazeres sensuais, afastado das qualidades não hábeis,
entra e permanece no primeiro jhana ... no segundo jhana ... no
terceiro jhana ... no quarto jhana. A isto se denomina a felicidade da renúncia,
a felicidade do afastamento, a felicidade da paz, a felicidade da iluminação.
Eu digo desse tipo de prazer que ele deve ser perseguido, ele deve ser
desenvolvido, ele deve ser cultivado e ele não deve ser temido.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
foi com referência a isso que foi dito: ’A pessoa deve saber como definir o
prazer e sabendo isso, deve buscar o prazer dentro de si.’ <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. “’A
pessoa não deve falar com a linguagem dissimulada, e não deve falar com a
linguagem não dissimulada, severa.’ Assim foi dito. E com referência a que foi
dito isso? <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
bhikkhus, quando a pessoa sabe que a linguagem dissimulada não é a verdade, é
incorreta e não traz benefício, ela não deve de forma nenhuma utilizá-la.
Quando a pessoa sabe que a linguagem dissimulada é a verdade, é correta e não
traz benefício, ela deve tentar não utilizá-la. Mas quando a pessoa sabe que a
linguagem dissimulada é a verdade, é correta e traz benefício, ela poderá
utilizá-la, sabendo o momento apropriado para fazê-lo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
bhikkhus, quando a pessoa sabe que a linguagem não dissimulada, severa, não é a
verdade, é incorreta e não traz benefício, ela não deve de forma nenhuma
utilizá-la. Quando a pessoa sabe que a linguagem não dissimulada, severa, é a
verdade, é correta e não traz benefício, ela deve tentar não utilizá-la. Mas
quando a pessoa sabe que a linguagem não dissimulada, severa, é a verdade, é
correta e traz benefício, ela poderá utilizá-la, sabendo o momento apropriado
para fazê-lo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
foi com referência a isso que foi dito: ’A pessoa não deve falar com a
linguagem dissimulada, e não deve falar com a linguagem não dissimulada,
severa.’ <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. “’A
pessoa deve falar sem pressa, não com pressa.’ Assim foi dito. E com referência
a o que foi dito isso?<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
bhikkhus, quando a pessoa fala com pressa, o corpo fica cansado e a mente fica
excitada, a voz fica tensa, a garganta fica rouca e a linguagem de quem fala
com pressa fica obscura e difícil de entender.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
bhikkhus, quando a pessoa fala sem pressa, o corpo não fica cansado nem a mente
fica excitada, a voz não fica tensa nem a garganta fica rouca e a linguagem de
quem fala sem pressa é clara e fácil de entender.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
foi com referência a isso que foi dito: ‘A pessoa deve falar sem pressa, não
com pressa.’<o:p></o:p></span></p>



<p class=Normal>12. “’A pessoa não deve insistir no idioma
local, e não deve anular o uso popular.’ Assim foi dito. E com referência a que
foi dito isso?<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Como,
bhikkhus, ocorre a insistência no idioma local e a anulação do uso popular?
Nesse caso, bhikkhus, em diferentes localidades eles chamam a mesma coisa um
‘prato’ <i>(pati), </i>uma ‘tigela’ <i>(patta), </i>um ‘vasilhame’ <i>(vittha),
</i>um ‘pires’ <i>(serava), </i>uma ‘panela’ <i>(dharopa), </i>um ‘pote’ <i>(pona),
</i>uma ‘caneca’ <i>(hana) </i>ou uma ‘bacia’ <i>(pisila). </i>Assim, seja qual
for a denominação em tal e tal localidade, a pessoa fala de forma
correspondente, aderindo com firmeza àquela expressão e insistindo que:
‘Somente isso é correto; todo o restante é errado.’ Assim é como ocorre a
insistência no idioma local e a anulação do uso popular. [<a href="#N6">6</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“E como, bhikkhus, não ocorre a insistência no idioma local nem a anulação
do uso popular? Nesse caso, bhikkhus, em diferentes localidades eles chamam a
mesma coisa um ‘prato’ ... ’bacia’<i>. </i>Assim seja qual for a denominação em
tal e tal localidade, sem aderir àquela expressão a pessoa fala de forma
correspondente, pensando: ‘Estes veneráveis, parece, estão falando com
referência a isto.’ Assim é como não ocorre a insistência no idioma local nem a
anulação do uso popular.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
foi com referência a isso que foi dito: ’A pessoa não deve insistir no idioma
local, e não deve anular o uso popular.’ <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. “Aqui,
bhikkhus, a busca do prazer, cuja satisfação está vinculada aos desejos
sensuais - baixos, vulgares, grosseiros, ignóbeis e que não trazem benefício –
é um estado atormentado pelo sofrimento, aflição, desespero e febre e é o
caminho errado. Portanto, este é um estado com conflito. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
bhikkhus, desengajar-se da busca do prazer, cuja satisfação está
vinculada aos desejos sensuais - baixos, vulgares, grosseiros, ignóbeis e que
não trazem benefício – é um estado sem sofrimento, aflição, desespero e febre e
é o caminho correto. Portanto, este é um estado sem conflito. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
bhikkhus, a busca da mortificação - dolorosa, ignóbil e que não traz
benefício - é um estado atormentado pelo sofrimento, aflição, desespero e febre
e é o caminho errado. Portanto, este é um estado com conflito. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
bhikkhus, desengajar-se da busca da mortificação - dolorosa, ignóbil e
que não traz benefício – é um estado sem sofrimento, aflição, desespero e febre
e é o caminho correto. Portanto, este é um estado sem conflito. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
bhikkhus, o Caminho do Meio descoberto pelo Tathagata evita ambos os extremos;
proporcionando visão, proporcionando conhecimento, conduzindo à paz, ao
conhecimento direto, à iluminação, a Nibbana. É um estado sem sofrimento ... e é
o caminho correto. Portanto, este é um estado sem conflito. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
bhikkhus, o enaltecimento, a depreciação e o fracasso em ensinar apenas o
Dhamma é um estado atormentado pelo sofrimento ... e é o caminho errado.
Portanto, este é um estado com conflito. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
bhikkhus, nem enaltecendo, nem depreciando, mas apenas ensinando o Dhamma é um
estado sem sofrimento ... e é o caminho correto. Portanto, este é um estado sem
conflito.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
bhikkhus, o prazer sensual – um prazer baixo, um prazer grosseiro, um prazer
ignóbil - é um estado atormentado pelo sofrimento ... Portanto, este é um estado
com conflito. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
bhikkhus, a felicidade da renúncia, a felicidade do afastamento, a felicidade
da paz, a felicidade da iluminação, é um estado sem sofrimento ... Portanto,
este é um estado sem conflito.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
bhikkhus, a linguagem dissimulada que não é a verdade, é incorreta e não traz
benefício é um estado atormentado pelo sofrimento ... Portanto, este é um estado
com conflito.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
bhikkhus, a linguagem dissimulada que é a verdade, é correta e não traz
benefício, é um estado atormentado pelo sofrimento ... Portanto, este é um
estado com conflito.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
bhikkhus, a linguagem dissimulada que é a verdade, é correta e traz benefício,
é um estado sem sofrimento ... Portanto, este é um estado sem conflito.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
bhikkhus, a linguagem não dissimulada, severa, que não é a verdade, é incorreta
e não traz benefício, é um estado atormentado pelo sofrimento ... Portanto, este
é um estado com conflito.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
