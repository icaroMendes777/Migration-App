<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
se alguém dissesse o seguinte: ‘Sem ter penetrado a nobre verdade do sofrimento
como ela na verdade é, sem ter penetrado a nobre verdade da origem do
sofrimento como ela na verdade é, sem ter penetrado a nobre verdade da cessação
do sofrimento como ela na verdade é, sem ter penetrado a nobre verdade do
caminho que conduz à cessação do sofrimento como ela na verdade é, eu darei um
fim completo ao sofrimento’ – isso é impossível.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Como,
bhikkhus, se alguém dissesse o seguinte: ‘Sem ter construído o andar térreo de
uma casa com cumeeira, eu irei construir o andar superior,’ isso seria
impossível; da mesma forma,<span style='mso-spacerun:yes'>  </span>se alguém
dissesse o seguinte: ‘Sem ter penetrado a nobre verdade do sofrimento como ela
na verdade é ... eu darei um fim completo ao sofrimento’ – isso é impossível.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas,
bhikkhus, se alguém dissesse o seguinte: ‘Tendo penetrado a nobre verdade do
sofrimento como ela na verdade é, tendo penetrado a nobre verdade da origem do
sofrimento como ela na verdade é, tendo penetrado a nobre verdade da cessação
do sofrimento como ela na verdade é, tendo penetrado a nobre verdade do caminho
que conduz à cessação do sofrimento como ela na verdade é, eu darei um fim
completo ao sofrimento’ – isso é possível..<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Como,
bhikkhus, se alguém dissesse o seguinte: ‘Tendo construído o andar térreo de
uma casa com cumeeira, eu irei construir o andar superior,’ isso seria
possível; da mesma forma,<span style='mso-spacerun:yes'>  </span>se alguém
dissesse o seguinte: ‘Tendo penetrado a nobre verdade do sofrimento como ela na
verdade é ... eu darei um fim completo ao sofrimento’ – isso é possível.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
bhikkhus, um esforço deve ser feito para compreender: ‘Isto é sofrimento.’ Um
esforço deve ser feito para compreender: ‘Esta é a origem do sofrimento.’ Um
esforço deve ser feito para compreender: ‘Esta é a cessação do sofrimento.’ Um
esforço deve ser feito para compreender: ‘Este é o caminho que conduz à
cessação do sofrimento.’”<o:p></o:p></span></p>

