<p class=Normal> Assim ouvi. Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no Parque de Anathapindika. </p>

<p class=Normal> “Bhikkhus, a pessoa comum sem instrução poderá experimentar o desencantamento em relação a este corpo composto dos quatro grandes elementos; ela poderá se tornar desapegada dele e libertada dele. Por que razão? Porque o crescimento e o declínio podem ser vistos neste corpo composto dos quatro grandes elementos, ele é visto sendo tomado e deixado de lado. Portanto, a pessoa comum sem instrução poderá experimentar o desencantamento em relação a este corpo composto dos quatro grandes elementos; ela poderá se tornar desapegada dele e libertada dele. </p>

<p class=Normal> “Mas, bhikkhus, quanto àquilo que é chamado ‘mente’ ou ‘mentalidade’ ou ‘consciência’ – a pessoa comum sem instrução é incapaz de experimentar o desencantamento em relação a isso, incapaz de se tornar desapegada e libertada disso. Por que razão? Porque, durante muito tempo, isso foi digno da confiança dela, foi tomado como propriedade dela e compreendido por ela da seguinte forma: ‘Isso é meu, isso sou eu, isso é o meu eu.’ Portanto, a pessoa comum sem instrução é incapaz de experimentar o desencantamento em relação à mente, incapaz de se tornar desapegada disso e libertada disso. </p>

<p class=Normal> “Seria melhor, bhikkhus, que uma pessoa comum sem instrução tomasse como o eu este corpo composto dos quatro grandes elementos ao invés da mente. Por que razão? Porque este corpo composto dos quatro grandes elementos é visto durando um ano, dois anos, três, quatro, cinco ou dez anos, vinte, trinta, quarenta, cinqüenta ou sessenta anos, cem anos ou até mais. Mas aquilo que é chamado ‘mente’ ou ‘mentalidade’ ou ‘consciência’ surge como uma coisa e cessa como outra de dia e de noite. </p>

<p class=Normal> “Nesse sentido, bhikkhus, o nobre discípulo bem instruído observa com cuidado e atenção a origem dependente da seguinte forma: 'Quando existe isso, aquilo existe; Com o surgimento disso, aquilo surge. Quando não existe isso, aquilo também não existe; Com a cessação disto, aquilo cessa.' Bhikkhus, na dependência de um contato para ser experimentado como prazeroso, bhikkhus, surge uma sensação prazerosa. Com a cessação daquele contato para ser experimentado como prazeroso, a correspondente sensação - a sensação prazerosa que surgiu na dependência daquele contato para ser experimentado como prazeroso – também cessa e é aplacada. </p>

<p class=Normal> “Na dependência de um contato para ser experimentado como doloroso, bhikkhus, surge uma sensação dolorosa. Com a cessação daquele contato para ser experimentado como doloroso, a correspondente sensação - a sensação dolorosa que surgiu na dependência daquele contato para ser experimentado como doloroso – também cessa e é aplacada. </p>

<p class=Normal> “Na dependência de um contato para ser experimentado como nem doloroso, nem prazeroso, bhikkhus, surge uma sensação nem dolorosa, nem prazerosa. Com a cessação daquele contato para ser experimentado como nem doloroso, nem prazeroso, a correspondente sensação - a sensação nem dolorosa, nem prazerosa que surgiu na dependência daquele contato para ser experimentado como nem doloroso, nem prazeroso – também cessa e é aplacada. </p>

<p class=Normal> “Bhikkhus, assim como o calor e o fogo são produzidos pela junção e fricção de dois gravetos de madeira, mas quando os gravetos são separados e postos de lado o calor resultante cessa e é aplacado, da mesma maneira na dependência de um contato para ser experimentado como prazeroso ... na dependência de um contato para ser experimentado como doloroso ... na dependência de um contato para ser experimentado como nem doloroso, nem prazeroso ... a sensação nem dolorosa, nem prazerosa que surgiu na dependência daquele contato para ser experimentado como nem doloroso, nem prazeroso – também cessa e é aplacada. </p>

<p class=Normal>"Vendo assim, bhikkhus, o nobre discípulo bem instruído experimenta o desencantamento em relação ao contato, desencantamento em relação à sensação, desencantamento em relação à percepção, desencantamento em relação às formações volitivas, desencantamento em relação à consciência. Experimentando o desencantamento ele se desapega. Através do desapego a sua mente é libertada. Quando ela está libertada surge o conhecimento: 'Libertada.' Ele compreende que: 'O nascimento foi destruído, a vida santa foi vivida, o que deveria ser feito, não há mais vir a ser a nenhum estado.'" </p>

