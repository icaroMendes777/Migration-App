<p class=Normal> Então o Venerável Ananda foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse: “Venerável senhor, um bhikkhu poderia alcançar uma tal concentração de modo que ele não daria atenção ao olho e às formas, ao ouvido e aos sons, ao nariz e aos odores, à língua e aos sabores, ao corpo e aos tangíveis; que (1) não daria atenção à terra, (2) água, (3) fogo, (4) ou ar; (5) não daria atenção à base do espaço infinito, (6) à base da consciência infinita, (7 ) à base do nada, (8) ou à base da nem percepção, nem não percepção; (9) não daria atenção a este mundo; (10) não daria atenção a outro mundo; (11) não daria atenção a qualquer coisa vista, ouvida, sentida, conscientizada, obtida, buscada, e reflexionada, e  ainda assim  a mente estaria atenta?"</p>

<p class=Normal>"Sim, Ananda, um bhikkhu poderia …"</p>

<p class=Normal>"Mas como, venerável  senhor, um bhikkhu poderia alcançar uma tal concentração de modo que ele não daria atenção ao olho e às formas … e  ainda assim  a mente estaria atenta?"</p>

<p class=Normal>"Aqui, Ananda, um bhikkhu daria atenção assim: 'Isto é a paz, isto é o sublime, isto é, o silenciar de todas as formações, o abandono de todas as aquisições, a destruição do desejo, desapego, cessação, Nibbana.' É desta forma, Ananda, que um bhikkhu poderia obter tal estado de concentração em que a mente não daria atenção ao olho e às formas, ao ouvido e aos sons, ao nariz e aos odores, à língua e aos sabores, ao corpo e aos tangíveis; que não daria atenção à terra, água, fogo, ou ar; não daria atenção à base do espaço infinito, à base da consciência infinita, à base do nada, ou à base da nem percepção, nem não percepção; não daria atenção a este mundo; não daria atenção a outro mundo; não daria atenção a qualquer coisa vista, ouvida, sentida, conscientizada, obtida, buscada, e reflexionada, e ainda assim a mente estaria atenta."</p>

