<p class=Normal> Assim, ouvi. Certa ocasião, o Abençoado estava em Savatthi, no Bosque de Jeta, no Parque de Anathapindika. Lá ele se dirigiu aos monges desta forma: “Bhikkhus”. – “Venerável Senhor”, eles responderam. O Abençoado disse o seguinte: </p>

<p class=Normal> "Bhikkhus, o discurso dotado de quatro características é bem falado, não é mal falado - irrepreensível e não será criticado pelos sábios, Quais quatro? Há o caso em que um bhikkhu diz apenas o que é bem falado, não o que é mal falado; apenas o que é justo, não o que é injusto, apenas o que é agradável, não o que é antipático, apenas o que é verdadeiro, não o que é falso. Um discurso dotado dessas quatro características é bem falado, não é mal falado - irrepreensível e não será criticado pelos sábios." </p>

<p class=Normal> Isso é o que disse o Abençoado. Tendo dito isso, o Mestre, o Iluminado disse mais: </p>

<p class=Normal style='margin-left:.75in'>Os calmos dizem que o que é bem falado é o melhor; <br>
em segundo lugar, que se deve dizer o que é justo, não o injusto; <br>
em terceiro lugar, o que é agradável, não o antipático; <br>
quarto, o que é verdadeiro, não o falso. </p>

<p class=Normal> Em seguida, o Venerável Vangisa, levantou do seu assento, arrumou o manto superior sobre o ombro, e olhando para o Abençoado com as mãos postas sobre o seu coração disse: "Tenho uma inspiração, Abençoado! Tenho uma inspiração, Iluminado!" </p>

<p class=Normal> "Revele a sua inspiração, Vangisa", disse o Abençoado. </p>

<p class=Normal> Em seguida, o Venerável Vangisa homenageou o Abençoado com estes versos cativantes: </p>


<p class=Normal style='margin-left:.75in'>Usemos palavras <br>
que não nos causem dor <br>
nem que tormentem os outros: <br>
essas são as palavras bem faladas. </p>

<p class=Normal style='margin-left:.75in'>Usemos palavras agradáveis, <br>
que alegrem as pessoas. <br>
Sem recorrer a palavras más. </p>

<p class=Normal style='margin-left:.75in'>As palavras da verdade são imortais: <br>
Tal como diz o velho ditado: <br>
palavras da verdade não morrem. <br>
E as boas pessoas, dizem, <br>
são bem estabelecidas na<br>
verdade, bem-estar e virtude. </p>

<p class=Normal style='margin-left:.75in'>O discurso do Abençoado, <br>
para a realização de Nibbana, <br>
dando um fim à massa de sofrimento: <br>
Esse é o discurso insuperável.</p>




