<p class=Normal>"Bhikkhus, o Tathagata possui esses quatro tipos de autoconfiança, possuindo-os ele reivindica o lugar de líder do rebanho, ruge o seu rugido de leão nas assembléias e coloca em movimento a roda de Brahma. Quais são os quatro? </p>

<p class=Normal> (1) "Eu não vejo o fundamento com base no qual algum contemplativo ou brâmane, ou deva, ou Mara, ou Brahma, ou qualquer outro no mundo pudesse, de acordo com o Dhamma, acusar-me do seguinte: 'Apesar de reivindicar a completa iluminação, você não é completamente iluminado com respeito a certas coisas, (dhammas).' E vendo nenhum fundamento nisso, eu permaneço seguro, destemido e autoconfiante. </p>

<p class=Normal> (2) "Eu não vejo o fundamento com base no qual algum contemplativo… ou qualquer outro no mundo pudesse me acusar do seguinte: 'Apesar de reivindicar ter destruído todas as impurezas, certas impurezas não estão destruídas por você.' E vendo nenhum fundamento nisso, eu permaneço seguro, destemido e autoconfiante. </p>

<p class=Normal> (3) "Eu não vejo o fundamento com base no qual algum contemplativo… ou qualquer outro no mundo pudesse me acusar do seguinte: 'Aquelas coisas que você chama de obstruções não são capazes de obstruir alguém que se ocupe com elas.' E vendo nenhum fundamento nisso, eu permaneço seguro, destemido e autoconfiante. </p>

<p class=Normal> (4) "Eu não vejo o fundamento com base no qual algum contemplativo… ou qualquer outro no mundo pudesse me acusar do seguinte: 'Ao ensinar para alguém o Dhamma, este não o conduz, quando praticado, à completa destruição do sofrimento.' E vendo nenhum fundamento nisso, eu permaneço seguro, destemido e autoconfiante. </p>

<p class=Normal>"Esses, bhikkhus são os quatro tipos de autoconfiança que o Tathagata possui, possuindo-os ele reivindica o lugar de líder do rebanho, ruge o seu rugido de leão nas assembléias e coloca em movimento a roda de Brahma." </p>

<p class=Normal>As doutrinas formuladas de distintas formas,<br>
das quais dependem os contemplativos e brâmanes, <br>
não tocam o Thatagata, <br>
o autoconfiante, que superou<br>
todas as doutrinas. </p>

<p class=Normal>Consumado, tendo superado tudo, <br>
colocando em movimento a roda do Dhamma<br>
por compaixão por todos os seres. <br>
Os seres o homenageiam, <br>
o melhor entre devas e humanos, <br>
que superou o ser/existir. </p>

