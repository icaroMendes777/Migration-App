<p class=Normal> Em certa ocasião, o Abençoado estava em Rajagaha, no Bambual, no Santuário dos Esquilos. Então o venerável Dabba Mallaputta foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse: "Agora, Abençoado, é o momento para o meu parinibbana."</p>

<p class=Normal> "Faça agora, Dabba, como julgar adequado."</p>

<p class=Normal> Então o venerável Dabba levantou do seu assento e depois de homenagear o Abençoado, mantendo-o à sua direita, elevou-se no ar, sentando de pernas cruzadas no céu, no espaço; entrando no elemento fogo, ao emergir ele realizou o parinibbana; o seu corpo queimou e foi totalmente consumido de modo que nem cinzas ou fuligem podiam ser vistos. Tal como quando manteiga ou óleo são queimados e consumidos, nem cinzas ou fuligem podem ser vistos, do mesmo modo, quando o venerável Dabba Mallaputta se elevou no ar, sentando de pernas cruzadas no céu, no espaço; entrando no elemento fogo, ao emergir ele realizou o parinibbana; o seu corpo queimou e foi totalmente consumido de modo que nem cinzas ou fuligem podiam ser vistos.</p>

<p class=Normal>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p>

<p class=Normal style='margin-left:.75in'> O corpo desintegrado, a percepção cessada,<br>
todas as sensações esfriadas,<br>
as atividades mentais acalmadas,<br>
a consciência chegou ao fim.</p>

