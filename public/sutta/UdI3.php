<p class=Normal style='margin-left:.75in'><span lang=PT-BR>Quando existe isso, aquilo existe.<br>
Com o surgimento disso, aquilo surge.<br>
Quando não existe isso, aquilo também não existe.<br>
Com a cessação disto, aquilo cessa. </span><span lang=PT-BR style='mso-bidi-font-size:
10.0pt'><o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR style='mso-ansi-language:PT-BR'>Em outras palavras: <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
&quot;Da ignorância como condição, as formações volitivas
[surgem]<br>
Das formações volitivas como condição, a consciência<br>
Da consciência como condição, a mentalidade-materialidade (nome e forma)<br>
Da mentalidade-materialidade (nome e forma) como condição, as seis bases dos sentidos<br>
Das seis bases dos sentidos como condição, o contato<br>
Do contato como condição, as sensações<br>
Das sensações como condição, o desejo<br>
Do desejo como condição, o apego<br>
Do apego como condição, ser/existir.<br>
Do ser/existir como condição, o nascimento<br>
Do nascimento como condição, o envelhecimento e morte, tristeza, lamentação,
dor, angústia e desespero<br>
Essa é a origem dessa massa de sofrimento. <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
&quot;Agora
da cessação sem deixar vestígios dessa mesma ignorância, cessam as formações volitivas.<br>
Da cessação das formações volitivas, cessa a consciência.<br>
Da cessação da consciência, cessa a mentalidade-materialidade (nome e forma).<br>
Da cessação da mentalidade-materialidade (nome e forma), cessam as seis bases dos sentidos.<br>
Da cessação das seis bases dos sentidos, cessa o contato.<br>
Da cessação do contato, cessam as sensações.<br>
Da cessação das sensações, cessa o desejo.<br>
Da cessação do desejo, cessa o apego.<br>
Da cessação do apego, cessa o ser/existir.<br>
Da cessação do ser/existir, cessa o nascimento.<br>
Da cessação do nascimento, então envelhecimento e morte, tristeza, lamentação,
dor, angústia e desespero, todos cessam. Essa é a cessação dessa massa de
sofrimento.</span><span lang=PT-BR style='mso-bidi-font-size:10.0pt;
mso-ansi-language:PT-BR'><o:p></o:p></span></p>

<p class=Normal>
<span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, dando-se conta do
significado disso, o Abençoado nessa ocasião exclamou: <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando os fenômenos ficam mais claros<br>
