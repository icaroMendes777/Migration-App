<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim ouvi.
Certa ocasião, o Abençoado estava em Uruvela às margens do rio Neranjara ao pé
de uma figueira-dos-pagodes, pouco tempo depois de ter alcançado a perfeita
iluminação – ele ficou sentado em meditação sob a sombra dessa árvore por sete
dias sem parar, sensível à felicidade da libertação. Ao final de sete dias,
após emergir daquela concentração, na terceira vigília da noite, ele considerou
minuciosamente a origem dependente em ordem progressiva e regressiva, dessa
forma:</span><span lang=PT-BR style='mso-bidi-font-size:10.0pt;mso-ansi-language:
