<p class=Normal>Assim ouvi. Certa ocasião, o Abençoado estava em Uruvela às margens do rio Neranjara ao pé de uma figueira-dos-pagodes, pouco tempo depois de ter alcançado a perfeita iluminação – ele ficou sentado em meditação sob a sombra dessa árvore por sete dias sem parar, sensível à felicidade da libertação. Ao final de sete dias, após emergir daquela concentração, na terceira vigília da noite, ele considerou minuciosamente a origem dependente em ordem regressiva, dessa forma:</p>

<p class=Normal style='margin-left:.75in'>Quando existe isso, aquilo existe. <br>
Com o surgimento disso, aquilo surge. <br>
Quando não existe isso, aquilo também não existe. <br>
Com a cessação disto, aquilo cessa. </p>

<p class=Normal style='margin-left:.75in'>Em outras palavras:</p>

<p class=Normal style='margin-left:.75in'> "Da cessação sem deixar vestígios dessa mesma ignorância, cessam as formações <br>
Da cessação das formações, cessa a consciência<br>
Da cessação da consciência, cessa a mentalidade-materialidade (nome e forma)<br>
Da cessação da mentalidade-materialidade (nome e forma), cessam as seis bases dos sentidos<br>
Da cessação das seis bases dos sentidos, cessa o contato<br>
Da cessação do contato, cessam as sensações<br>
Da cessação das sensações, cessa o desejo<br>
Da cessação do desejo, cessa o apego<br>
Da cessação do apego, cessa o ser/existir<br>
Da cessação do ser/existir, cessa o nascimento<br>
Da cessação do nascimento, então envelhecimento e morte, tristeza, lamentação, dor, angústia e desespero, todos cessam. Essa é a cessação dessa massa de sofrimento.</p>

<p class=Normal>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p>

<p class=Normal style='margin-left:.75in'>Quando os fenômenos ficam mais claros<br>
para o brâmane – ardente, absorto -<br>
todas as suas dúvidas desaparecem pois ele compreende<br>
a completa destruição das condições.</p>

