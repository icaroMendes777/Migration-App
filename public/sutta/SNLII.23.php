</span>em Savatthi à margem do Sutanu. Então, um grande número de bhikkhus
foram até o Venerável Anuruddha e eles se cumprimentaram. Quando a conversa
cortês e amigável havia terminado, eles sentaram a um lado e disseram para o
Venerável Anuruddha:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Por ter
desenvolvido e cultivado quais coisas é que o Venerável Anuruddha alcançou a
supremacia do conhecimento direto?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigos, é
porque eu desenvolvi e cultivei os quatro fundamentos da atenção plena que
alcancei a supremacia do conhecimento direto. Quais quatro? Aqui, amigos, eu
permaneci contemplando o corpo como um corpo, ardente, plenamente consciente e
com atenção plena, tendo colocado de lado a cobiça e o desprazer pelo mundo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu
permaneci contemplando as sensações como sensações ... mente como mente ... objetos
mentais como objetos mentais, ardente, plenamente consciente e com atenção
plena, tendo colocado de lado a cobiça e o desprazer pelo mundo.<o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Amigos, é porque eu desenvolvi e cultivei os quatro fundamentos da
atenção plena que alcancei a supremacia do conhecimento direto. Além disso,
amigos, é porque eu desenvolvi e cultivei os quatro fundamentos da atenção
plena que por meio do olho divino, que é purificado e ultrapassa o humano, vejo
seres falecendo e renascendo, inferiores e superiores, bonitos e feios,
afortunados e desafortunados. Compreendo como os seres prosseguem de acordo com
as suas ações desta forma: ‘Esses seres – dotados de má conduta com o corpo,
linguagem e mente, que insultam os nobres, com o entendimento incorreto e
realizando ações sob a influência do entendimento incorreto – com a dissolução
do corpo, após a morte, renasceram no plano de privação, um destino ruim, os
planos inferiores, no inferno. Porém estes seres - dotados de boa conduta com o
corpo, linguagem e mente,<span style="mso-spacerun: yes">  </span>ue não insultam
os nobres, com o<span style="mso-spacerun: yes">  </span>entendimento correto e
realizando ações sob a influência do entendimento correto – com a dissolução do
corpo, após a morte, renasceram num bom destino, no paraíso.’ Dessa forma - por
meio do olho divino, que é purificado e ultrapassa o humano, eu vejo seres
falecendo e renascendo, inferiores e superiores, bonitos e feios, e compreendo
como os seres continuam de acordo com as suas ações.”<o:p></o:p></span></p>


