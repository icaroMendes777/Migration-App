<p class=Normal> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: "Bhikkhus, tal como os elementos as pessoas se unem e se associam umas às outras. Seres com a disposição inferior se unem e se associam aos seres com disposição inferior. Seres com boa disposição se unem e se associam aos seres com boa disposição. Foi assim no passado, será assim no futuro, assim é no presente."</p>

<p class=Normal style='margin-left:.75in'> O desejo que nasce da associação <br>
é cortado através da não-associação. <br>
Tal como alguém sobre uma tábua <br>
afundaria no grande oceano, <br>
assim também alguém virtuoso<br>
afunda ao se associar com um vadio.</p>

<p class=Normal style='margin-left:.75in'>Portanto evite os vadios, <br>
que fazem pouco esforço. <br>
Permaneça com aqueles que permanecem isolados, <br>
os nobres determinados, meditadores, <br>
que são dedicados e sábios.</p>


