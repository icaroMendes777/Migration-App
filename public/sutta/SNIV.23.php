<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>Notas:</b><o:p></o:p></span></p>



<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] “Libertação temporária da mente” são as realizações meditativas
mundanas, isto é, os jhanas e as realizações imateriais,
assim chamadas porque nos momentos de absorção a mente está livre dos
obstáculos. Ele decaía dessa libertação por motivo de doença crônica que o
impedia de satisfazer todas as condições para a concentração. [<a href="#R1">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[2] Um eufemismo para o suicídio. [<a href="#R2">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[3] Mara pensou: “esse contemplativo deseja usar a faca. Isso indica que
ele está indiferente em relação ao corpo e à vida, e uma pessoa assim é capaz
de alcançar o estado de arahant. Se eu tentar proibí-lo ele não desistirá, mas
se o Mestre proibí-lo ele obedecerá.” Assim, dando a impressão de estar
preocupado com o destino do bhikkhu, Mara foi até o Abençoado. [<a href="#R3">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[4] O bhikkhu cortou a veia jugular com a faca. Sensações dolorosas
surgiram. Ele as conteve, compreendeu as dores com insight, estabeleceu a
atenção plena, investigou o objeto da meditação e realizou o estado de arahant,
realizando o estado de arahant simultaneamente com o fim da vida. [<a href="#R4">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N5></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[5] Mara buscava pela consciência de renascimento, <i>(patisandhicitta),
</i>mas Godhika faleceu com a consciência de renascimento não estabelecida. Mas
com isso não se deve entender que depois da morte a consciência dele sobreviveu
numa condição “não estabelecida”, pois muitos textos deixam claro que com o
falecimento do arahant a sua consciência também cessa (Veja por exemplo o <a
href="SNXII.51.php">SN XII.51</a>). [<a href="#R5">Retorna</a>] <o:p></o:p></span></p>




