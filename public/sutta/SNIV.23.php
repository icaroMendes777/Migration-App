<p class=Normal style='margin-left:.75in'>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim ouvi.
Em certa ocasião, o Abençoado estava<span style="mso-spacerun: yes">  </span>em
Rajagaha, no Bambual, no Santuário dos Esquilos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Agora,
naquela ocasião o venerável Godhika estava na Rocha Negra na encosta do
Isigili. Então, enquanto o venerável Godhika permanecia diligente, ardente e
decidido ele alcançou a libertação temporária da mente, mas decaiu daquela
libertação temporária da mente. [<a href="#N1">1</a>] <a name=R1></a>Uma
segunda vez, enquanto o venerável Godhika permanecia diligente, ardente e
decidido ele alcançou a libertação temporária da mente, mas decaiu daquela
libertação temporária da mente. Uma terceira vez ... uma quarta vez ... uma
quinta vez ... Uma sexta vez, enquanto o venerável Godhika permanecia
diligente, ardente e decidido ele alcançou a libertação temporária da mente,
mas decaiu daquela libertação temporária da mente. Uma sétima vez, enquanto o
venerável Godhika permanecia diligente, ardente e decidido ele alcançou a
libertação temporária da mente, mas decaiu daquela libertação temporária da
mente.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, o
venerável Godhika pensou: “Eu já decaí sete vezes dessa libertação temporária
da mente. Vou usar a faca.” [<a href="#N2">2</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Então, Mara, o Senhor do Mal, tomando conhecimento com a sua mente do
pensamento na mente do venerável Godhika, foi até o Abençoado e se dirigiu a
ele em versos: [<a href="#N3">3</a>]<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><a name=R3></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>“Oh grande herói, eminente em sabedoria, <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>resplandecente em poder e glória!<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Eu venero seus pés, Aquele com Visão,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>que superou toda inimizade e medo.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Oh grande herói que conquistou a morte,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>o seu discípulo anseia pela morte.<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Ele intenciona [se matar]:<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>impeça-o de fazer isso, Oh Luminoso!<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Como, Oh Abençoado, pode um seu discípulo -<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>que se delicia com o ensinamento,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>um praticante buscando o ideal da mente - <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>tirar a própria vida, Oh Afamado?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Agora,
naquela ocasião o venerável Godhika havia acabado de usar a faca. [<a href="#N4">4</a>]
<a name=R4></a>Então, o Abençoado, tendo compreendido, “Este é Mara, o Senhor
do Mal,” respondeu em forma de versos:<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Assim de fato é como os determinados agem:<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>eles não têm apego à vida.<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Extirpando o desejo pela raiz,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Godhika realizou o parinibbana.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Nessa ocasião,
o Abençoado se dirigiu aos bhikkhus: “Venham, bhikkhus, vamos até a Rocha Negra
na encosta do Isigili, onde Godhika usou a faca.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
venerável senhor”, aqueles bhikkhus responderam. Então, o Abençoado, juntamente
com um grande número de bhikkhus, foi até a Rocha Negra na encosta do Isigili.
O Abençoado viu à distância o venerável Godhika deitado na cama com os ombros
virados. <o:p></o:p></span></p>



<p class=Normal><a name=mara></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>Agora,
naquela ocasião uma nuvem de fumaça, um redemoinho de trevas, estava se movendo
para o leste, depois para o oeste, para o norte, para o sul, para cima, para
baixo e para os quadrantes intermediários. Então, o Abençoado se dirigiu aos
bhikkhus: “Vocês vêm, bhikkhus, aquela nuvem de fumaça, um redemoinho de
trevas, movendo-se para o leste, depois para o oeste, para o norte, para o sul,
para cima, para baixo e para os quadrantes intermediários?&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
venerável senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aquilo,
bhikkhus, é Mara, o Senhor do Mal, procurando pela consciência de Godhika,
perguntando a si mesmo: ‘Onde agora se estabeleceu a consciência de Godhika? No
entanto, bhikkhus, com a consciência não estabelecida, Godhika realizou o
parinibbana.” [<a href="#N5">5</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R5></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Nesse momento, Mara o Senhor do Mal, tomando um alaúde, foi até o
Abençoado e se dirigiu a ele em versos:<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Acima, abaixo e dos lados, <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>nos quatro quadrantes e no meio, <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>estive procurando mas não encontro<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>para onde Godhika foi.”<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[O Abençoado:]<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Aquele homem determinado era decidido,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>um meditador sempre se deliciando com a meditação,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>dedicando-se dia e noite<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>sem apego nem sequer à vida.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Com a conquista do exército da Morte,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>não retornou para uma nova existência,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>ao extirpar o desejo pela raiz,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Godhika realizou o parinibbana.”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Ele ficou tão triste pela decepção

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>que o alaúde caiu do seu braço.

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Como resultado, aquele espírito desapontado

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>desapareceu no mesmo instante.<span style='mso-tab-count:1'>           </span><o:p></o:p></span></p>




