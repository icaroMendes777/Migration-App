<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião o Abençoado estava em Savathi, no Bosque de Jeta, no
Parque de Anathapindika. Lá ele se dirigiu aos monges desta forma: “Bhikkhus”.
– “Venerável Senhor”, eles responderam. O Abençoado disse o seguinte:<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.
“Bhikkhus, quando um pastor possui onze fatores, ele é incapaz de manter e
criar um rebanho de gado. Quais onze? É o caso em que um pastor não tem
conhecimento da forma, ele não tem habilidade com as características, ele não
consegue remover os ovos das moscas, ele não consegue tratar as feridas, ele
não consegue fumigar o galpão, ele não sabe onde está a fonte d’água, ele não
sabe o que deve ser bebido, ele não conhece a estrada, ele não tem habilidade
com os pastos, ele não sabe como ordenhar e ele não demonstra uma veneração
especial para com aqueles touros que são os pais e lideres do rebanho. Quando
um pastor possui esses onze fatores ele é incapaz de manter e criar um rebanho
de gado. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. “Da mesma forma,
bhikkhus, quando um bhikkhu possui onze qualidades ele não é suscetível de
crescimento, desenvolvimento e realização neste Dhamma e Disciplina. Quais
onze? É o caso em que um bhikkhu não tem conhecimento da forma, ele não tem
habilidade com as características, ele não consegue remover os ovos das moscas,
ele não consegue tratar as feridas, ele não consegue fumigar o galpão, ele não
sabe onde está a fonte d’água, ele não sabe o que deve ser bebido, ele não
conhece a estrada, ele não tem habilidade com os pastos, ele não sabe como
ordenhar e ele não demonstra uma veneração especial para com aqueles bhikkhus
seniores com larga experiência e que há muito tempo seguiram a vida santa,
os pais e líderes da Sangha.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. “Como é que um bhikkhu
não tem conhecimento da forma? Neste caso um bhikkhu não compreende como na
verdade é: ‘Toda forma material de qualquer tipo consiste dos quatro grandes
elementos e da forma material derivada dos quatro grandes elementos.’ Assim é
como um bhikkhu não tem conhecimento da forma.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. “Como é que um bhikkhu
não tem habilidade com as características? Neste caso um bhikkhu não compreende
como na verdade é: ‘Um tolo é caracterizado por suas ações; um sábio é
caracterizado por suas ações.’ Assim é como um bhikkhu não tem habilidade com
as características. [<a href="#N1">1</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R1></a> 6. “</span><span
lang=PT-BR style='mso-ansi-language:PT-BR'>Como é que um bhikkhu não consegue
remover os ovos das moscas? Neste caso, quando já surgiu um pensamento de
desejo sensual, um bhikkhu o tolera; ele não o abandona, não o remove, não o
elimina, não o aniquila. Quando já surgiu um pensamento de má vontade ...
Quando já surgiu um pensamento de crueldade ... Quando já surgiram estados
ruins e prejudiciais, um bhikkhu os tolera; ele não os abandona, não os remove,
não os elimina, não os aniquila. Assim é como um bhikkhu não consegue remover
os ovos das moscas.</span><span lang=PT-BR style='font-size:11.0pt;mso-bidi-font-size:
12.0pt;mso-ansi-language:PT-BR'><o:p></o:p></span></p>

<p class=Normal> 7. </span><span lang=PT-BR
style='mso-ansi-language:PT-BR'>“Como é que um bhikkhu não consegue tratar as
feridas? Neste caso, ao ver uma forma com o olho, um bhikkhu se agarra aos seus
sinais ou detalhes. Permanecendo com a<span style="mso-spacerun: yes"> 
</span>faculdade do olho descuidada, ele poderá ser tomado por estados ruins e
prejudiciais de cobiça e tristeza e apesar disso, ele não pratica a contenção,
ele não protege a faculdade do olho, ele não se empenha na contenção da
faculdade do olho. Ao ouvir um som com o ouvido ... Ao cheirar um aroma com o
nariz ... Ao saborear um sabor com a língua ... Ao tocar algo tangível com o
corpo ... Ao conscientizar um objeto mental com a mente ele se agarra aos seus
sinais ou detalhes. Permanecendo com a<span style="mso-spacerun: yes"> 
</span>faculdade da mente descuidada, ele poderá ser tomado por estados ruins e
prejudiciais de cobiça e tristeza e apesar disso, ele não pratica a contenção,
ele não protege a faculdade da mente, ele não se empenha na contenção da
faculdade da mente. Assim é como um bhikkhu não consegue tratar as feridas.<o:p></o:p></span></p>

<p class=Normal> 8. </span><span lang=PT-BR
style='mso-ansi-language:PT-BR'>“Como é que um bhikkhu não consegue fumigar o
galpão? Neste caso um bhikkhu não ensina aos outros, em detalhe, o Dhamma da
forma como ele aprendeu e dominou. Assim é como um bhikkhu não consegue fumigar
o galpão.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9.
“Como é que um bhikkhu não sabe onde está a fonte d’água? Neste caso um bhikkhu
não procura, de tempos em tempos, aqueles bhikkhus que aprenderam muito, que
conhecem bem as tradições, que mantêm o Dhamma, a Disciplina e os Códigos [<a
href="#N2">2</a>] <a name=R2></a>e não os questiona e pergunta da seguinte
forma: ‘Como é isto, venerável senhor? Qual o significado disto?’ Esses
veneráveis não revelam o que não foi revelado, não esclarecem o que não está
claro ou removem as suas dúvidas acerca das várias coisas que fazem surgir a
dúvida. Assim é como um bhikkhu não sabe onde está a fonte d’água.</span><span
lang=PT-BR style='font-size:11.0pt;mso-bidi-font-size:12.0pt;mso-ansi-language:
PT-BR'><o:p></o:p></span></p>

<p class=Normal><a name=R5></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>10. “Como é que um bhikkhu não sabe o que deve ser bebido? Neste caso,
quando o Dhamma e a Disciplina do Tathagata estão sendo ensinados, um bhikkhu
não obtém inspiração do significado, não obtém inspiração do Dhamma, não obtém
satisfação do Dhamma. [<a href="#N3">3</a>] <a name=R3></a><span
style="mso-spacerun: yes"> </span>Assim é como um bhikkhu não sabe o que deve
ser bebido.</span><span lang=PT-BR style='font-size:11.0pt;mso-bidi-font-size:
12.0pt;mso-ansi-language:PT-BR'><o:p></o:p></span></p>

<p class=Normal><a name=R6></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. “Como é
que um bhikkhu não conhece a estrada? Neste caso um bhikkhu não compreende como
na verdade é o Nobre Caminho Óctuplo. Assim é como um bhikkhu não conhece a
estrada.<o:p></o:p></span></p>

<p class=Normal><a name=T12></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. “Como é que um bhikkhu
não tem habilidade com os pastos? Neste caso um bhikkhu não compreende como na
verdade são os quatro fundamentos da atenção plena. Assim é como um bhikkhu não
tem habilidade com os pastos. [<a href="#N4">4</a>]</span><span
lang=PT-BR style='mso-ansi-language:PT-BR'><o:p></o:p></span></p>

<p class=Normal><a name=R4></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. “Como é
que um bhikkhu não sabe como ordenhar? Neste caso, quando chefes de família
devotos convidam um bhikkhu a tomar o quanto queira de mantos, alimentos,
moradia e medicamentos, o bhikkhu não tem moderação ao aceitar. Assim é como um
bhikkhu não sabe como ordenhar. </span><span lang=PT-BR style='font-size:11.0pt;
mso-bidi-font-size:12.0pt;mso-ansi-language:PT-BR'><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14. “Como é que um bhikkhu
não demonstra uma veneração especial para com aqueles bhikkhus mais velhos com
muita experiência e que há muito tempo seguiram a vida santa, os pais e líderes
da Sangha? Neste caso um bhikkhu não pratica atos de amor bondade com o corpo
para com esses bhikkhus mais velhos, tanto em público como em particular; ele
não pratica atos de amor bondade com a linguagem para com esses bhikkhus mais
velhos, tanto em público como em particular; não pratica atos de amor bondade com
a mente para com esses bhikkhus mais velhos, tanto em público como em
particular. Assim é como um bhikkhu não demonstra uma veneração especial para
com aqueles bhikkhus mais velhos com muita experiência e que há muito tempo
seguiram a vida santa, os pais e líderes da Sangha.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“ Quando um bhikkhu possui
essas onze qualidades ele não é suscetível de crescimento, desenvolvimento e
realização neste Dhamma e Disciplina.<o:p></o:p></span></p>

<p class=Normal><a name=R8></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>15.
“Bhikkhus, quando um pastor possui onze fatores, ele é capaz de manter e criar
um rebanho de gado. Quais onze? É o caso em que um pastor tem conhecimento da
forma, ele tem habilidade com as características, ele consegue remover os ovos
das moscas, ele consegue tratar as feridas, ele consegue fumigar o galpão, ele
sabe onde está a fonte d’água, ele sabe o que deve ser bebido, ele conhece a
estrada, ele tem habilidade com os pastos, ele sabe como ordenhar e ele
demonstra uma veneração especial para com aqueles touros que são os pais e
lideres do rebanho. Quando um pastor possui esses onze fatores ele é capaz de
manter e criar um rebanho de gado.<o:p></o:p></span></p>

<p class=Normal><a name=R12></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>16. “Da
mesma forma, bhikkhus, quando um bhikkhu possui onze qualidades ele é
suscetível de crescimento, desenvolvimento e realização neste Dhamma e
Disciplina. Quais onze? É o caso em que um bhikkhu tem conhecimento da forma,
ele tem habilidade com as características, ele consegue remover os ovos das
moscas, ele consegue tratar as feridas, ele consegue fumigar o galpão, ele sabe
onde está a fonte d’água, ele sabe o que deve ser bebido, ele conhece a estrada,
ele tem habilidade com os pastos, ele sabe como ordenhar e ele demonstra uma
veneração especial para com aqueles bhikkhus mais velhos com muita experiência
e que há muito tempo seguiram a vida santa, os pais e líderes da Sangha<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17. “Como é que um bhikkhu
tem conhecimento da forma? Neste caso um bhikkhu compreende como na verdade é:
‘Toda forma material de qualquer tipo consiste dos quatro grandes elementos e
da forma material derivada dos quatro grandes elementos’. Assim é como um
bhikkhu compreende a forma. <o:p></o:p></span></p>

<p class=Normal><a name=R13></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>18. “Como é
que um bhikkhu tem habilidade com as características? Neste caso um bhikkhu
compreende como na verdade é: ‘Um tolo é caracterizado por suas ações; uma
pessoa sábia é caracterizada por suas ações’. Assim é como um bhikkhu tem
habilidade com as características.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>19. Como é que um
bhikkhu consegue remover os ovos das moscas? Neste caso, quando já surgiu um
pensamento de desejo sensual, um bhikkhu não o tolera; ele o abandona, o
remove, o elimina, o aniquila. Quando já surgiu um pensamento de má vontade ...
Quando já surgiu um pensamento de crueldade ... Quando já surgiram estados
ruins e prejudiciais, um bhikkhu não os tolera; ele os abandona, os remove, os
elimina, os aniquila. Assim é como um bhikkhu consegue remover os ovos das
moscas.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>20. “Como é que um bhikkhu
consegue tratar as feridas? Neste caso, ao ver uma forma com o olho, um bhikkhu
não se agarra aos seus sinais <span style="mso-spacerun: yes"> </span>ou
detalhes. Ele permanece cuidando da faculdade do olho, não sendo tomado por
estados ruins e prejudiciais de cobiça e tristeza, ele pratica a contenção, ele
protege a faculdade do olho, ele se empenha na contenção da faculdade do olho.
Ao ouvir um som com o ouvido ... Ao cheirar um aroma com o nariz ... Ao
saborear um sabor com a língua ... Ao tocar algo tangível com o corpo ... Ao
conscientizar um objeto mental com a mente ele não se agarra aos seus sinais <span
style="mso-spacerun: yes"> </span>ou detalhes. Ele permanece cuidando da<span
style="mso-spacerun: yes">  </span>faculdade da mente, não sendo tomado por
estados ruins e prejudiciais de cobiça e tristeza, ele pratica a contenção, ele
protege a faculdade da mente, ele se empenha na contenção da faculdade da
mente. Assim é como um bhikkhu consegue tratar as feridas.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>21. “Como é que um bhikkhu
consegue fumigar o galpão? Neste caso, um bhikkhu ensina aos outros, em
detalhe, o Dhamma da forma como ele aprendeu e dominou. Assim é como um bhikkhu
consegue fumigar o galpão.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>22. “Como é que um bhikkhu
sabe onde está a fonte d’água?<span style="mso-spacerun: yes">  </span>Neste
caso, um bhikkhu procura, de tempos em tempos, aqueles bhikkhus que aprenderam
muito, que conhecem bem as tradições, que mantêm o Dhamma, a Disciplina e os
Códigos e os questiona e pergunta da seguinte forma: ‘Como é isto, venerável
senhor? Qual o significado disto?’ Esses veneráveis revelam o que não foi
revelado, esclarecem o que não está claro ou removem as suas dúvidas acerca das
várias coisas que fazem surgir a dúvida. Assim é como um bhikkhu sabe onde está
a fonte d’ água.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>23. “Como é
que um bhikkhu sabe o que deve ser bebido? Neste caso, quando o Dhamma e a
Disciplina do Tathagata estão sendo ensinados, um bhikkhu obtém inspiração do
significado, obtém<span style="mso-spacerun: yes">  </span>inspiração do
Dhamma, obtém satisfação do Dhamma. Assim é como um bhikkhu sabe o que deve ser
bebido.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>24. “Como é
que um bhikkhu conhece a estrada? Neste caso, um bhikkhu compreende como na
verdade é o Nobre Caminho Óctuplo. Assim é como um bhikkhu conhece a estrada.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>25. “Como é
que um bhikkhu tem habilidade com os pastos? Neste caso, um bhikkhu compreende
como na verdade são os quatro fundamentos da atenção plena. Assim é como um
bhikkhu tem habilidade com os pastos.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>26. “Como é
que um bhikkhu sabe como ordenhar? Neste caso, quando chefes de família devotos
convidam um bhikkhu a tomar o quanto queira de mantos, alimentos, moradia e
medicamentos, o bhikkhu tem moderação ao aceitar. Assim é como um bhikkhu sabe
como ordenhar.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>27. “Como é
que um bhikkhu demonstra uma veneração especial para com aqueles bhikkhus seniores
 com muita experiência e que há muito tempo seguiram a vida santa, os
pais e líderes da Sangha? Neste caso, um bhikkhu pratica atos de amor bondade
com o corpo para com esses bhikkhus seniores, tanto em público como em
particular; ele pratica atos de amor bondade com a linguagem para com esses
bhikkhus seniores, tanto em público como em particular; pratica atos de amor
bondade com a mente para com esses bhikkhus seniores, tanto em público como
em particular. Assim é como um bhikkhu demonstra uma veneração especial para
com aqueles bhikkhus seniores com muita experiência e que há muito tempo
seguiram a vida santa, os pais e líderes da Sangha.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando um
bhikkhu possui<span style="mso-spacerun: yes">  </span>essas onze qualidades
ele é suscetível de crescimento, desenvolvimento e realização neste Dhamma e
Disciplina<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o
que disse o Abençoado. Os bhikkhus ficaram satisfeitos e contentes com as
palavras do Abençoado.<o:p></o:p></span></p>


