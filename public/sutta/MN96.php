<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
Ouvi. Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no
Parque de Anathapindika.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. Então, o
brâmane Esukari foi até o Abençoado e ambos se cumprimentaram. Quando a
conversa cortês e amigável havia terminado, ele sentou a um lado e disse:<o:p></o:p></span></p>



<p class=Normal>3. “Mestre Gotama, os brâmanes
prescrevem quatro níveis de serviço. <span lang=PT-BR style='mso-ansi-language:
PT-BR'>Eles prescrevem um nível de serviço para um brâmane, um nível de serviço
para um nobre, um nível de serviço para um comerciante e um nível de serviço
para um trabalhador. Nesse sentido, Mestre Gotama, os brâmanes prescrevem o
seguinte nível de serviço para um brâmane: um brâmane pode servir a um brâmane,
um nobre pode servir a um brâmane, um comerciante pode servir a um brâmane e um
trabalhador pode servir a um brâmane. Esse é o nível de serviço para um brâmane
que os brâmanes prescrevem. Mestre Gotama, os brâmanes prescrevem o seguinte
nível de serviço para um nobre: um nobre pode servir a um nobre, um comerciante
pode servir a um nobre e um trabalhador pode servir a um nobre. Esse é o nível
de serviço para um nobre que os brâmanes prescrevem. Mestre Gotama, os brâmanes
prescrevem o seguinte nível de serviço para um comerciante: um comerciante pode
servir a um comerciante e um trabalhador pode servir a um comerciante. Esse é o
nível de serviço para um comerciante que os brâmanes prescrevem. Mestre Gotama,
os brâmanes prescrevem o seguinte nível de serviço para um trabalhador: apenas
um trabalhador pode servir a um trabalhador; pois quem mais poderia servir a um
trabalhador? Esse é o nível de serviço para um trabalhador que os brâmanes prescrevem.
</span>O que o Mestre Gotama diz a respeito disso?”</p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. “Bem,
brâmane, o mundo todo autorizou os brâmanes a prescreverem esses quatro níveis
de serviço?” – “Não, Mestre Gotama.” – “Suponha, brâmane, que alguém empurrasse
um naco de carne para um homem pobre, sem um tostão, destituído, dizendo: ‘Bom
homem, você deve comer esta carne e pagar por ela’; da mesma forma, sem o
consentimento dos demais contemplativos e brâmanes, os brâmanes apesar disso
prescrevem esses quatro níveis de serviço.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. “Eu não digo,
brâmane, que todos devem ser servidos, nem eu digo que ninguém deve ser
servido. Pois se, ao servir alguém, a pessoa se torna pior e não melhor devido
ao serviço, então eu digo que ela não deve servir. E se, ao servir
alguém, a pessoa se torna melhor e não pior devido ao serviço, então eu
digo que ela deve servir.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. “Se eles
perguntassem a um nobre o seguinte: ‘Quais desses você serviria – aquele em
cujo serviço você se tornasse pior e não melhor ao serví-lo, ou aquele em cujo
serviço você se tornasse melhor e não pior ao serví-lo: respondendo da forma
correta, um nobre responderia o seguinte: ‘Eu não deveria servir aquele em cujo
serviço eu me tornasse pior e não melhor ao serví-lo; eu deveria servir aquele
em cujo serviço eu me tornasse melhor e não pior ao serví-lo.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se eles
perguntassem a um brâmane … perguntassem a um comerciante ... perguntassem a um
trabalhador … respondendo da forma correta, um trabalhador responderia o
seguinte: ‘Eu não deveria servir aquele em cujo serviço eu me tornasse pior e
não melhor ao serví-lo; eu deveria servir aquele em cujo serviço eu me tornasse
melhor e não pior ao serví-lo.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. “Eu não
digo, brâmane, que alguém seja melhor porque é de uma família nobre,
nem eu digo que alguém seja pior porque é de uma família nobre. Eu não
digo que alguém seja melhor porque possui notável beleza, nem eu digo que
alguém seja pior porque possui notável beleza. Eu não digo que alguém seja
melhor porque possui grande riqueza, nem eu digo que alguém seja pior porque
possui grande riqueza.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. “Pois
aqui, brâmane, alguém de uma família nobre poderá matar seres vivos,
tomar aquilo que não for dado, portar-se de forma imprópria em relação aos
prazeres sensuais, dizer mentiras, falar com malícia, falar de modo grosseiro,
falar de modo frívolo, ser cobiçoso, ter uma mente com má vontade e ter
entendimento incorreto. Portanto, eu não digo que alguém seja melhor porque é
de uma família nobre. Mas também, brâmane, alguém de uma família nobre
poderá<span style="mso-spacerun: yes">  </span>abster-se de matar seres vivos,
de tomar aquilo que não for dado, de portar-se de forma imprópria em relação
aos prazeres sensuais, de dizer mentiras, de falar com malícia, de falar de
modo grosseiro, de falar de modo frívolo, e ele poderá não ser cobiçoso, não
ter uma mente com má vontade e ter entendimento correto. Portanto, eu não digo
que alguém seja pior porque é de uma família nobre.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
brâmane, alguém que possui notável beleza … alguém que possui grande riqueza poderá
matar seres vivos ... e ter entendimento incorreto. Portanto, eu não digo que
alguém seja melhor porque possui notável beleza ... possui grande riqueza. Mas
também, brâmane, alguém que possui notável beleza … possui grande riqueza
poderá abster-se de matar seres vivos ... e ter entendimento correto. Portanto,
eu não digo que alguém seja pior porque possui notável beleza ... possui grande
riqueza.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. “Eu não
digo, brâmane, que todos devem ser servidos, nem eu digo que ninguém deve ser
servido. Pois se, ao servir alguém, a fé, virtude, aprendizado, generosidade e
sabedoria da pessoa crescem por estar a serviço dele, então eu digo que ele
deve servir.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. Quando
isso foi dito o brâmane Esukari disse para o Abençoado: “Mestre Gotama, os
brâmanes prescrevem quatro tipos de riqueza. Eles prescrevem a riqueza de um
brâmane, a riqueza de um nobre, a riqueza de um comerciante e a riqueza de um
trabalhador.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Nesse
sentido, Mestre Gotama, os brâmanes prescrevem isto como a riqueza de um
brâmane – esmolar alimentos; [<a href="#N1">1</a>] <a name=R1></a>um brâmane
que menospreza a própria riqueza, a esmola de alimentos, abusa do seu dever
como um guarda que toma aquilo que não lhe foi dado. Essa é a riqueza de um
brâmane prescrita pelos brâmanes. Mestre Gotama, os brâmanes prescrevem isto
como a riqueza de um nobre – o arco e a aljava; um nobre que menospreza a
própria riqueza, o arco e a aljava, abusa do seu dever como um guarda que toma
aquilo que não lhe foi dado. Essa é a riqueza de um nobre prescrita pelos brâmanes.<span
style="mso-spacerun: yes">  </span>Mestre Gotama, os brâmanes prescrevem isto
como a riqueza de um comerciante – a agricultura e a pecuária; [<a href="#N2">2</a>]
<a name=R2></a>um comerciante que menospreza a própria riqueza, a agricultura e
a pecuária, abusa do seu dever como um guarda que toma aquilo que não lhe foi
dado. Essa é a riqueza de um comerciante prescrita pelos brâmanes. Mestre
Gotama, os brâmanes prescrevem isto como a riqueza de um trabalhador – a foice
e a trave de carga; um trabalhador que menospreza a própria riqueza, a foice e
a trave de carga, abusa do seu dever como um guarda que toma aquilo que não lhe
foi dado. Essa é a riqueza de um trabalhador prescrita pelos brâmanes. </span>O
que o Mestre Gotama diz a respeito disso?”</p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. “Bem,
brâmane, o mundo todo autorizou os brâmanes a prescreverem esses quatro tipos
de riqueza?” – “Não, Mestre Gotama.” - “Suponha, brâmane, que alguém empurrasse
um naco de carne para um homem pobre, sem um tostão, destituído, dizendo: ‘Bom
homem, você deve comer esta carne e pagar por ela’; da mesma forma, sem o
consentimento dos demais contemplativos e brâmanes, os brâmanes apesar disso
prescrevem esses quatro tipos de riqueza.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. “Eu,
brâmane, declaro o nobre e supramundano Dhamma como a riqueza da própria
pessoa. [<a href="#N3">3</a>] <a name=R3></a>Pois, recordando a sua linhagem
familiar ancestral maternal e paternal, ela é reconhecida de acordo com o lugar
onde tenha renascido, onde quer que seja. [<a href="#N4">4</a>] <a name=R4></a>Se
ela renascer num clã de nobres, ela será reconhecida como um nobre; se ela renascer
num clã de brâmanes, ela será reconhecida como um brâmane; se ela renascer num
clã de comerciantes, ela será reconhecida como um comerciante; se ela renascer
num clã de trabalhadores, ela será reconhecida como um trabalhador. Da mesma
forma como o fogo é reconhecido pela condição particular na dependência da qual
ele arde – quando o fogo arde na dependência de lenha, ele é reconhecido como
fogo de lenha; quando o fogo arde na dependência de gravetos, ele é reconhecido
como fogo de gravetos; quando o fogo arde na dependência de capim, ele é
reconhecido como fogo de capim; quando o fogo arde na dependência de esterco de
vaca, ele é reconhecido como fogo de esterco de vaca<span style="mso-spacerun:
yes">  </span>- assim também, brâmane, eu declaro o nobre e supramundano Dhamma
como a riqueza da própria pessoa.<span style="mso-spacerun: yes">  </span>Pois,
recordando a sua linhagem familiar ancestral maternal e paternal, alguém é
reconhecido de acordo com o lugar onde tenha renascido, onde quer que seja. Se
ele renascer ... num clã de trabalhadores, ele é reconhecido como um trabalhador.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. “Se,
brâmane, qualquer um de um clã de nobres deixa a vida em família pela vida
santa, e depois de haver encontrado o Dhamma e Disciplina proclamados pelo
Tathagata, ele se abstém de matar seres vivos, de tomar aquilo que não for
dado, do não celibato, da linguagem mentirosa, da linguagem maliciosa, da
linguagem grosseira e da linguagem frívola, e não é cobiçoso, tem uma mente sem
má vontade e tem entendimento correto, ele é um dos que está realizando o
verdadeiro caminho, o Dhamma que é benéfico. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se,
brâmane, qualquer um de um clã de brâmanes deixa a vida em família … qualquer
um de um clã de comerciantes deixa a vida em família ... qualquer um de um clã
de trabalhadores deixa a vida em família pela vida santa, e depois de haver
encontrado o Dhamma e Disciplina proclamados pelo Tathagata, ele se abstém de
matar seres vivos, de tomar aquilo que não for dado, do não celibato, da
linguagem mentirosa, da linguagem maliciosa, da linguagem grosseira e da
linguagem frívola, e não é cobiçoso, tem uma mente sem má vontade e tem
entendimento correto, ele é um dos que está realizando o verdadeiro caminho, o
Dhamma que é benéfico.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14. “O que
você pensa, brâmane? Apenas um brâmane é capaz de desenvolver uma mente imbuída
de amor bondade para com uma certa região, sem hostilidade e sem má vontade, e
não um nobre ou um comerciante, ou um trabalhador?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
Mestre Gotama. Quer seja um nobre, ou um brâmane, ou um comerciante, ou um
trabalhador – </span>todos aqueles das quatro castas<span lang=PT-BR
style='mso-ansi-language:PT-BR'> são capazes de desenvolver uma mente imbuída
de amor bondade para com uma certa região, sem hostilidade e sem má vontade.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Da mesma
forma, brâmane, se qualquer um de um clã de nobres deixa a vida em família ... (repetir
o verso 13) ... ele é um dos que está realizando o verdadeiro caminho, o Dhamma
que é benéfico.<o:p></o:p></span></p>



<p class=Normal>15. <span lang=PT-BR style='mso-ansi-language:PT-BR'>“O que
você pensa, brâmane? Apenas um brâmane é capaz de tomar uma esponja e sabão, ir
até o rio e limpar o pó e a sujeira, e não um nobre ou um comerciante, ou um
trabalhador?”</span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
Mestre Gotama. Quer seja um nobre, ou um brâmane, ou um comerciante, ou um
trabalhador – </span>todos aqueles das quatro castas<span lang=PT-BR
style='mso-ansi-language:PT-BR'> são capazes de tomar uma esponja e sabão, ir
até o rio e limpar o pó e a sujeira.”</span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Da mesma
forma, brâmane, se qualquer um de um clã de nobres deixa a vida em família ...
(repetir o verso 13) ... ele é um dos que está realizando o verdadeiro caminho,
o Dhamma que é benéfico</span>.</p>


<p class=Normal>16. <span lang=PT-BR style='mso-ansi-language:PT-BR'>“O que
você pensa, brâmane?</span> Suponha que um nobre rei ungido aqui reunisse cem
homens com nascimentos díspares … (igual ao <a href="MN93.php#T11">MN 93 verso
11</a>) … Pois todo fogo possui uma chama, cor e luminosidade, e é possível
usar qualquer fogo para a finalidade de um fogo.”</p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Da mesma
forma, brâmane, se qualquer um de um clã de nobres deixa a vida em família ...
(repetir o verso 13) ... ele é um dos que está realizando o verdadeiro caminho,
o Dhamma que é benéfico.”</span></p>


<p class=Normal>17. Quando isso foi dito, o brâmane Esukari disse para o
Abençoado: “Magnífico, Mestre Gotama! Magnífico, Mestre Gotama! Mestre Gotama
esclareceu o Dhamma de várias formas, como se tivesse colocado em pé o que
estava de cabeça para baixo, revelasse o que estava escondido, mostrasse o
caminho para alguém que estivesse perdido ou segurasse uma lâmpada no escuro
para aqueles que possuem visão pudessem ver as formas. Eu busco refúgio no
Mestre Gotama, no Dhamma e na Sangha dos bhikkhus. Que o Mestre Gotama me
aceite como discípulo leigo que buscou refúgio para o resto da vida.”</p>


