<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em Savathi. ‘Bhikkhus, eu ensinarei para vocês o caminho que conduz à
origem da identidade e o caminho que conduz à cessação da identidade. Ouçam e
prestem muita atenção àquilo que eu vou dizer.” – “Sim, venerável senhor,” os
bhikkhus responderam. O Abençoado disse o seguinte:<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é o caminho que conduz à origem da identidade? Aqui, bhikkhus, a
pessoa comum sem instrução, que não respeita os nobres, que não é proficiente
nem treinada no Dhamma deles, que não respeita os homens verdadeiros, que
não é proficiente nem treinada no Dhamma deles, considera que a forma é o
eu, considera o eu como possuído de forma material, considera a forma material
como estando no eu, considera o eu como estando na forma material, considera que a
sensação é o eu ... considera que a percepção é o eu ... considera que as formações volitivas são o eu ... considera que a
consciência é o eu, considera o eu como possuído de consciência, considera a consciência
como estando no eu, consciência o eu como estando na consciência. Isso, bhikkhus, é
chamado o caminho que conduz à origem do sofrimento. Quando é dito, ‘O caminho
que conduz à origem da identidade’, o significado neste caso é: um modo de
considerar as coisas, que conduz à origem do sofrimento.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é o caminho que conduz à cessação da identidade? Aqui, bhikkhus, um
nobre discípulo bem instruído, que respeita os nobres, que é proficiente e
treinado no Dhamma deles, que respeita os homens verdadeiros, que é
proficiente e treinado no Dhamma deles, não considera que a forma é o eu, não considera o eu 
como possuído de forma material, não considera a forma material
como estando no eu, não considera o eu como estando na forma material, 
não considera que a sensação é o eu ... não considera que a percepção é o eu ... não considera que as formações volitivas são o
eu ... não considera que a consciência é o eu, não considera o eu 
como possuído de consciência, não considera a consciência
como estando no eu, não considera o eu como estando na consciência. Isso,
bhikkhus, é chamado o caminho que conduz à cessação do sofrimento. Quando é
dito, ‘O caminho que conduz à cessação da identidade’, o significado neste caso
é: um modo de considerar as coisas, que conduz à cessação do sofrimento.<b
