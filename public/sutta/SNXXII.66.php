<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. </span>Então um certo bhikkhu foi até o Abençoado e depois de
cumprimentá-lo sentou a um lado e disse<span lang=PT-BR style='mso-ansi-language:
PT-BR'>:<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, seria bom se o Abençoado pudesse me ensinar o Dhamma de forma resumida,
de modo que tendo ouvido o Dhamma do Abençoado eu possa permanecer só, isolado,
diligente, ardente e decidido.”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhu,
você deve abandonar o desejo por tudo que é impermanente.”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu
compreendo, Abençoado! Eu Compreendo, Iluminado!”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como,
bhikkhu, você compreende o significado em detalhe do que eu disse de forma
resumida?”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“A forma,
venerável senhor, é impermanente; eu devo abandonar o desejo por ela. A
sensação é impermanente ... a percepção é impermanente ... as formações volitivas são
impermanente ... a consciência é impermanente; eu devo abandonar o desejo por
ela.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Senhor,
assim é como compreendo o significado em detalhe do que o Abençoado disse de
forma resumida.”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Muito bem,
bhikkhu. É bom que seja assim que você tenha entendido o significado em detalhe
do que eu disse de forma resumida. A forma é impermanente ... a consciência é
impermanente; você deve abandonar o desejo por ela. Assim é<span
style="mso-spacerun: yes">  </span>como o significado em detalhe do que eu
disse de forma resumida deve ser entendido.”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o
bhikkhu, </span>satisfeito e contente com as palavras do Abençoado<span
lang=PT-BR style='mso-ansi-language:PT-BR'>, levantou do seu assento e depois
de homenagear o Abençoado, mantendo-o à sua direita, partiu. Depois,
permanecendo só, isolado, diligente, ardente e decidido, </span>em pouco tempo<span
lang=PT-BR style='mso-ansi-language:PT-BR'>, ele alcançou e permaneceu no
objetivo supremo da vida santa pelo qual membros de um clã deixam a vida em
família pela vida santa, </span>tendo conhecido e realizado por si mesmo no
aqui e agora<span lang=PT-BR style='mso-ansi-language:PT-BR'>. Ele soube: “O
nascimento foi destruído, a vida santa foi vivida, o que deveria ser feito foi
feito, não há mais vir a ser a nenhum estado.” E assim ele se tornou mais um
dos arahants.<b style='mso-bidi-font-weight:normal'><o:p></o:p></b></span></p>


