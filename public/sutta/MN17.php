...<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15-18.
“Aqui, bhikkhus, um bhikkhu vive na dependência de uma certa cidade ...<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>19-22.
