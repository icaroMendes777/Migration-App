<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>23. “Aqui,
bhikkhus, um bhikkhu vive na dependência de uma certa pessoa ... (igual ao
verso 3) ... Esse bhikkhu deve partir sem se despedir daquela pessoa ; ele não
deve mais seguí-la.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>24. “Aqui,
bhikkhus, um bhikkhu vive na dependência de uma certa pessoa ... (igual ao
verso 4) ... Tendo refletido dessa forma, esse bhikkhu deve partir depois de se
despedir daquela pessoa;[<a href="#N3">3</a>] <a name=R3></a>ele não deve mais
