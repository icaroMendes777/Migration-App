<p class=Normal style='margin-left:.75in'>





<p class=Normal>Em certa ocasião o Abençoado estava próximo a Benares no Parque do Gamo
em Isipatana. Lá ele se
dirigiu aos monges desta forma: “Bhikkhus” – “Venerável Senhor,” eles
responderam. O Abençoado disse o seguinte: <o:p></o:p></span></p>





<p class=Normal>“Certa vez, bhikkhus, havia um rei cujo nome era<span
style="mso-spacerun: yes">  </span>Pacetana. Certo dia o rei Pacetana disse ao
seu construtor de carruagens, 'Meu estimado construtor de carruagens, em seis
meses ocorrerá uma batalha. Você pode me fazer um novo par de rodas de
carruagem?' <o:p></o:p></span></p>





<p class=Normal>&quot;'Sim majestade, eu posso,' o construtor de carruagens respondeu
ao rei. <o:p></o:p></span></p>





<p class=Normal>&quot;Então em seis meses menos seis dias o construtor de carruagens
terminou uma roda. O rei Pacetana lhe disse, ‘Em seis dias ocorrerá a batalha.
O par de rodas de carruagem estará pronto?' <o:p></o:p></span></p>





<p class=Normal>&quot;'Majestade, nesses seis meses menos seis dias, eu terminei uma
roda.' <o:p></o:p></span></p>





<p class=Normal>&quot;'Porém você pode terminar a segunda roda nesses seis dias?' <o:p></o:p></span></p>





<p class=Normal>&quot;'Sim majestade, eu posso,' o construtor de carruagens respondeu
ao rei. <o:p></o:p></span></p>





<p class=Normal>&quot;Então, após terminar a segunda roda em seis dias, o construtor de
carruagens levou o par de rodas para o rei e, chegando, lhe disse, ‘Aqui está o
seu novo par de rodas totalmente concluído, majestade.' <o:p></o:p></span></p>





<p class=Normal>&quot;'E qual é a diferença entre a roda que demorou seis meses menos
seis dias para terminar, e a roda que demorou seis dias para terminar? Eu não
vejo qualquer diferença entre as duas.' <o:p></o:p></span></p>





<p class=Normal>&quot;'Existe uma diferença entre as duas, majestade. Veja a
diferença.' Então o construtor de carruagens tomou a roda que demorou seis dias
para terminar e colocou-a em movimento. Indo até onde o seu impulso a levou,
ela girou em torno de si e caiu no solo. Então ele tomou a roda terminada em seis
meses menos seis dias e a colocou em movimento. Indo até onde o seu impulso a
levou, ela parou como se estivesse presa a um eixo. <o:p></o:p></span></p>





<p class=Normal>&quot;'Agora qual é a razão, estimado construtor de carruagens, qual é a
causa, porque a roda terminada em seis dias, quando colocada em movimento, vai
até onde o seu impulso a leva e então girando em torno de si, cai no solo? E
qual é a razão, qual é a causa, porque a roda da carruagem terminada em seis
meses menos seis dias, quando colocada em movimento, vai até onde o seu impulso
a leva e então para como se estivesse presa a um eixo?' <o:p></o:p></span></p>





<p class=Normal>&quot;'Majestade, quanto à roda terminada em seis dias, a sua borda
está deformada, com falhas e defeitos. Os seus raios estão deformados, com falhas
e defeitos. O seu cubo está deformado, com falhas e defeitos. Porque a sua
borda ... raios ... e cubo estão deformados, com falhas e defeitos, quando
colocada em movimento ela <span style="mso-spacerun: yes"> </span>vai até onde
o seu impulso a leva e então girando em torno de si, cai no solo. Porém quanto à
roda terminada em seis meses menos seis dias,<span style="mso-spacerun: yes"> 
</span>a sua borda não está deformada, sem falhas e defeitos. Os seus raios não
estão deformados, sem falhas e defeitos. O seu cubo não está deformado, sem
falhas e defeitos. Porque a sua borda ... raios ... e cubo não estão
deformados, sem falhas e defeitos, quando colocada em movimento ela vai até
onde o seu impulso a leva e então para como se estivesse presa a um eixo.' <o:p></o:p></span></p>



<p class=Normal>&quot;Agora, bhikkhus, o pensamento lhes pode ocorrer que o construtor
de carruagens naquela ocasião era uma outra pessoa, porém isto não deve ser
visto assim. Eu mesmo era o construtor de carruagens naquela ocasião. Eu tinha
habilidade em lidar com a deformidade, as falhas, os defeitos da madeira. Agora
sou um arahant, perfeitamente iluminado, hábil em lidar com as deformidades, falhas
e defeitos da ação corporal; hábil em lidar com as deformidades, falhas e
defeitos da ação verbal; hábil em lidar com as deformidades, falhas e defeitos
da ação mental. <o:p></o:p></span></p>


<p class=Normal>&quot;Qualquer bhikkhu ou bhikkhuni em quem a deformidade,
defeitos e falhas da ação corporal não forem abandonadas; a deformidade,
defeitos e falhas da ação verbal não forem abandonadas; a deformidade, defeitos
e falhas da ação mental não forem abandonadas terá se afastado deste Dhamma e
Disciplina, tal como a roda terminada em seis dias. Porém qualquer bhikkhu ou
bhikkhuni em quem a deformidade, defeitos e falhas da ação corporal são
abandonados; a deformidade, defeitos e falhas da ação verbal são abandonados; a
deformidade, defeitos e falhas da ação mental são abandonados permanece firme
neste Dhamma e Disciplina, tal como a roda terminada em seis meses menos
seis dias. <o:p></o:p></span></p>


<p class=Normal> &quot;Dessa forma vocês devem treinar: 'Nós abandonaremos a
deformidade, os defeitos e falhas na ação corporal. Nós abandonaremos a
deformidade, os defeitos e falhas na ação verbal. Nós abandonaremos a
deformidade, os defeitos e falhas na ação mental.’ Assim é como vocês devem
treinar.” <o:p></o:p></span></p>

