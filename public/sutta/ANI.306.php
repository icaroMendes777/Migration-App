<p class=Normal> (306) "Bhikkhus, eu não vejo nenhuma outra única coisa, que faz com que qualidades prejudiciais que ainda não surgiram surjam, e qualidades benéficas que já surgiram declinem, como o entendimento incorreto. Para quem tem o entendimento incorreto, as qualidades prejudiciais que ainda não surgiram surgem, e as qualidades benéficas que já surgiram declinam." </p>


<p class=Normal> (307) "Bhikkhus, eu não vejo nenhuma outra única coisa, que faz com que qualidades benéficas que ainda não surgiram surjam e qualidades prejudiciais que já surgiram diminuam, como o entendimento correto. Para quem tem o entendimento correto as qualidades benéficas que ainda não surgiram surgem, e qualidades prejudiciais que já surgiram declinam." </p>


<p class=Normal> (308) "Bhikkhus, eu não vejo nenhuma outra única coisa, que faz com que qualidades benéficas que ainda não surgiram não surjam, e qualidades benéficas que já surgiram declinem, como o entendimento incorreto. Para quem tem o entendimento incorreto, as qualidades benéficas que ainda não surgiram não surgem, e as qualidades benéficas que já surgiram declinam." </p>


<p class=Normal> (309) "Bhikkhus, eu não vejo nenhuma outra única coisa, que faz com que qualidades prejudiciais que ainda não surgiram não surjam e qualidades prejudiciais que já surgiram diminuam, como o entendimento correto. Para quem tem o entendimento correto as qualidades prejudiciais que ainda não surgiram não surgem, e qualidades prejudiciais que já surgiram diminuem." </p>


<p class=Normal> (310) "Bhikkhus, eu não vejo nenhuma outra única coisa, que faz com que o entendimento incorreto que ainda não surgiu surja, e o entendimento incorreto que já surgiu aumente, como a atenção sem sabedoria. Para quem tem atenção sem sabedoria, o entendimento incorreto que ainda não surgiu surge, e o entendimento incorreto que já surgiu aumenta." </p>


<p class=Normal> (311) "Bhikkhus, eu não vejo nenhuma outra única coisa, que faz com que o entendimento correto que ainda não surgiu surja, e o entendimento correto que já surgiu aumente, como a atenção com sabedoria. Para quem tem atenção com sabedoria, o entendimento correto que ainda não surgiu surge, e o entendimento correto que já surgiu aumenta." </p>


<p class=Normal> (312) "Bhikkhus, eu não vejo nenhuma outra única coisa por conta da qual, com a dissolução do corpo, após a morte, os seres renasçam num plano de miséria, num destino ruim, nos mundos inferiores, no inferno, como o entendimento incorreto. Possuindo o entendimento incorreto, com a dissolução do corpo, após a morte, os seres renascem num plano de miséria, num destino ruim, nos mundos inferiores, no inferno." </p>


<p class=Normal> (313) "Bhikkhus, eu não vejo nenhuma outra única coisa por conta da qual, com a dissolução do corpo, após a morte, os seres renasçam num destino feliz, no paraíso como o entendimento correto. Possuindo o entendimento correto, com a dissolução do corpo, após a morte, os seres renascem num destino feliz, no paraíso." </p>


<p class=Normal> (314) "Bhikkhus, uma pessoa com o entendimento incorreto, qualquer kamma corporal, kamma verbal ou kamma mental que ela incite e realize, de acordo com esse entendimento, e qualquer que seja a sua intenção, anseio, inclinação, e atividades volitivas, tudo irá conduzir ao que não é desejado, não é querido, ao que é desagradável, ao dano e ao sofrimento. Por qual razão? Porque o entendimento é ruim. </p>


<p class=Normal>"Suponham, bhikkhus, que uma semente de pepino amargo ou de abóbora amarga fosse plantada num solo fértil. Quaisquer nutrientes que a semente tome do solo e da água irão resultar no seu sabor amargo, pungente, e desagradável. Por qual razão? Porque a semente é ruim. Assim também, uma pessoa com o entendimento incorreto ... tudo irá conduzir ao que não é desejado, não é querido, ao que é desagradável, ao dano e ao sofrimento. Por qual razão? Porque o entendimento é ruim." </p>


<p class=Normal> (315) "Bhikkhus, uma pessoa com o entendimento correto qualquer kamma corporal, kamma verbal ou kamma mental que ela incite e realize, de acordo com esse entendimento, e qualquer que seja a sua intenção, anseio, inclinação, e atividades volitivas, tudo irá conduzir ao que é desejado, é querido, ao que é agradável, ao bem-estar e à felicidade. Por qual razão? Porque o entendimento é bom. </p>


<p class=Normal>"Suponham, bhikkhus, que uma semente de cana-de-açúcar, de arroz, ou uva,  fosse plantada num solo fértil. Quaisquer nutrientes que a semente tome do solo e da água irão resultar no seu sabor doce, agradável, e delicioso. Por qual razão? Porque a semente é boa. Assim também, uma pessoa com o entendimento correto ... tudo irá conduzir ao que é desejado, é querido, ao que é agradável, ao bem-estar e à felicidade. Por qual razão? Porque o entendimento é bom." </p>


<p class=Normal> (316) “Bhikkhus, há a pessoa que surge neste mundo em prejuízo de muitos, pela infelicidade de muitos, pela ruína, dano e sofrimento de muitos, devas e humanos. Quem é essa pessoa? É a pessoa com o entendimento incorreto, com a perspectiva incorreta. Ela afasta muitas pessoas do bom Dhamma e as estabelece no Dhamma ruim. Essa é a pessoa que surge neste mundo em prejuízo de muitos, pela infelicidade de muitos, pela ruína, dano e sofrimento de muitos, devas e humanos." </p>


<p class=Normal> (317) “Bhikkhus, há a pessoa que surge neste mundo em benefício de muitos, pela felicidade de muitos, pelo bem, bem-estar e felicidade de muitos, devas e humanos. Quem é essa pessoa? É a pessoa com o entendimento correto, com a perspectiva correta. Ela afasta muitas pessoas do Dhamma ruim e as estabelece no bom Dhamma. Essa é a pessoa que surge neste mundo em benefício de muitos, pela felicidade de muitos, pelo bem, bem-estar e felicidade de muitos, devas e humanos." </p>


<p class=Normal> (318) “Bhikkhus, eu não vejo outra coisa que seja tão repreensível como o entendimento incorreto. O entendimento incorreto é a pior das coisas que são repreensíveis.”
</p>

