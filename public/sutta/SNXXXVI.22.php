<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
eu ensinarei para vocês um discurso do Dhamma sobre as cento e oito
sensações.<span style="mso-spacerun: yes">  </span><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual, bhikkhus, é o
discurso do Dhamma sobre as cento e oito sensações? Em uma exposição eu falei
sobre dois tipos de sensações; em outra exposição eu falei sobre três tipos de
sensações; eu falei de cinco tipos de sensações ... seis tipos de sensações ...
dezoito tipos de sensações ... trinta e seis tipos de sensações ... cento e
oito tipos de sensações. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E quais, bhikkhus, são os
dois tipos de sensações? Sensações corporais e mentais. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E quais, bhikkhus, são os três
tipos de sensações? Sensações prazerosas, sensações dolorosas e sensações nem
dolorosas, nem prazerosas. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E quais, bhikkhus, são os
cinco tipos de sensações?<span style="mso-spacerun: yes">  </span>A faculdade
do prazer, a faculdade da dor, a faculdade da alegria, a faculdade da tristeza e
a faculdade da equanimidade. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E quais, bhikkhus, são os
seis tipos de sensações? As sensações que nascem por meio do contato no olho,
ouvido, nariz, língua, corpo e mente. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E quais, bhikkhus, são os
dezoito tipos de sensações?<span style="mso-spacerun: yes">  </span>Seis
examinações acompanhadas pela alegria, seis examinações acompanhadas pela
tristeza, seis examinações acompanhadas pela equanimidade. [<a href="#N1">1</a>]
<o:p></o:p></span></p>

<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E quais,
bhikkhus, são os trinta e seis tipos de sensações?<span style="mso-spacerun:
yes">  </span>Existem seis sensações de alegria baseadas na vida em família e
seis sensações de alegria baseadas na renúncia; seis sensações de tristeza
baseadas na vida em família e seis sensações de tristeza baseadas na renúncia;
seis sensações de equanimidade baseadas na vida em família e seis sensações de
equanimidade baseadas na renúncia.[<a href="#N2">2</a>] <o:p></o:p></span></p>

<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E quais,
bhikkhus, são os cento e oito tipos de sensações? São as trinta e seis
sensações acima no passado; são as trinta e seis sensações acima no futuro e
são as trinta e seis sensações acima no presente.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Esse, bhikkhus, é o
discurso do Dhamma sobre as cento e oito sensações.” <o:p></o:p></span></p>


