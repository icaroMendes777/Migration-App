<p class=Normal>(303) "Bhikkhus, para o conhecimento direto da cobiça, cinco coisas devem ser desenvolvidas. Quais cinco? A percepção do repulsivo, a percepção da morte, a percepção do perigo, a percepção do repulsivo na comida, e a percepção do não-deleite com tudo no mundo. Para o conhecimento direto da cobiça, essas cinco coisas devem ser desenvolvidas."</p>

<p class=Normal>(304) "Bhikkhus, para o conhecimento direto da cobiça, cinco coisas devem ser desenvolvidas. Quais cinco? A percepção da impermanência, a percepção de não-eu, a percepção da morte, a percepção do repulsivo na comida, e a percepção do não-deleite com tudo no mundo. Para o conhecimento direto da cobiça, essas cinco coisas devem ser desenvolvidas."</p>

<p class=Normal>(305) "Bhikkhus, para o conhecimento direto da cobiça, cinco coisas devem ser desenvolvidas. Quais cinco? A percepção da impermanência, a percepção do sofrimento naquilo que é impermanente, a percepção de não-eu naquilo que é sofrimento, a percepção de abandono, a percepção de desapego. Para o conhecimento direto da cobiça, essas cinco coisas devem ser desenvolvidas."</p>

<p class=Normal>(306) "Bhikkhus, para o conhecimento direto da cobiça, cinco coisas devem ser desenvolvidas. Quais cinco? A faculdade da convicção, a faculdade da energia, a faculdade da atenção plena, a faculdade da concentração, a faculdade da sabedoria. Para o conhecimento direto da cobiça, essas cinco coisas devem ser desenvolvidas."</p>

<p class=Normal>(307) "Bhikkhus, para o conhecimento direto da cobiça, cinco coisas devem ser desenvolvidas. Quais cinco? O poder da convicção, o poder da energia, o poder da atenção plena, o poder da concentração, o poder da sabedoria. Para o conhecimento direto da cobiça, essas cinco coisas devem ser desenvolvidas."</p>

<p class=Normal>(308-316) "Bhikkhus, para a completa compreensão da cobiça ... para a completa destruição ... para o abandono ... para a destruição ... para o desaparecimento ... para o desapego ... para a cessação ... para abrir mão ... para a renúncia da cobiça, cinco coisas devem ser desenvolvidas. Quais cinco? A percepção do repulsivo ... [todos os cinco conjuntos acima, até] ... O poder da convicção, o poder da energia, o poder da atenção plena, o poder da concentração, o poder da sabedoria. Para a renúncia da cobiça, essas cinco coisas devem ser desenvolvidas."</p>

<p class=Normal>(317-1152) "Bhikkhus, para o conhecimento direto ... para a completa compreensão .... para a completa destruição ... para o abandono ... para a destruição ... para o desaparecimento ... para o desapego ... para a cessação .... para abrir mão ... para a renúncia da raiva ... da delusão ... do ódio ... da hostilidade ... da difamação ...  da insolência ... da inveja ... da avareza ... da enganação ... da astúcia ... da teimosia .... da veemência ... da presunção ... da arrogância ... da embriaguez .... da negligência, cinco coisas devem ser desenvolvidas. Quais cinco? A percepção do repulsivo ... [todos os cinco conjuntos de cinco acima, até] ... O poder da convicção, o poder da energia, o poder da atenção plena, o poder da concentração, o poder da sabedoria. Para a renúncia da negligência, essas cinco coisas devem ser desenvolvidas." </p>


