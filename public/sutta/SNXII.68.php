<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&nbsp;Em
certa ocasião o venerável Musila, venerável Pavittha, venerável Narada e o venerável Ananda estavam
em Kosambi no monastério de Ghosita.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o
venerável Pavittha disse para o venerável Musila, “Musila, meu amigo, colocando de lado a
fé, colocando de lado a preferência, colocando de lado a tradição, colocando de
lado a razão, colocando de lado as idéias, você verdadeiramente possui
conhecimento pessoal assim: ‘Do nascimento como condição, o envelhecimento e
morte [surge]’?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
Pavittha, meu amigo. Colocando de lado a fé ... preferência ... tradição ... razão ... as idéias, eu verdadeiramente possuo conhecimento pessoal assim: ‘Do
nascimento como condição, o envelhecimento e morte.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(Do mesmo
modo com, ‘Do ser/existir como condição, o nascimento’...’Do apego como
condição, o ser/existir’ ... ’Do desejo como condição, o apego’ ... ’Da
sensação como condição, o desejo’ ... ’Do contato como condição, a sensação’
... ’Das seis bases dos sentidos como condição, o contato’ ... ’Da  mentalidade-materialidade (nome e forma)</span><span
lang=PT-BR style='mso-ansi-language:PT-BR'> como condição, as seis bases dos
sentidos’ ... ’Da consciência como condição, a mentalidade-materialidade (nome e forma)</span><span
lang=PT-BR style='mso-ansi-language:PT-BR'>’ ... ’Das formações volitivas como condição,
a consciência.’)<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Musila,
meu amigo, colocando de lado a fé, colocando de lado a preferência, colocando
de lado a tradição, colocando de lado a razão, colocando de lado as
idéias, você verdadeiramente possui conhecimento pessoal assim: ‘Da ignorância
como condição, as formações volitivas’?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
Pavittha, meu amigo. Colocando de lado a fé ... preferência ... tradição ... razão ... as idéias, eu verdadeiramente possuo conhecimento pessoal assim: ‘Da
ignorância como condição, as formações volitivas.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Musila,
meu amigo, colocando de lado a fé, colocando de lado a preferência, colocando
de lado a tradição, colocando de lado a razão, colocando de lado as
idéias, você verdadeiramente possui conhecimento pessoal assim: ‘Da cessação do
nascimento, cessa o envelhecimento e morte’?”<span style='mso-spacerun:yes'> 
</span><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
Pavittha, meu amigo. Colocando de lado a fé, colocando de lado a preferência,
colocando de lado a tradição, colocando de lado a razão, colocando de
lado as idéias, eu verdadeiramente possuo conhecimento pessoalassim: ‘Da
cessação do nascimento, cessa o envelhecimento e morte.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(Do mesmo
modo com ‘Da cessação do ser/existir, cessa o nascimento’ ... ’Da cessação do
apego, cessa o ser/existir’ ... ’Da cessação do desejo, cessa o apego’ ... ’Da
cessação da sensação, cessa o desejo’ ... ’Da cessação do contato, cessa a
sensação’ ... ’Da cessação das seis bases dos sentidos, cessa o contato’ ...
’Da cessação da mentalidade-materialidade (nome e forma),</span><span lang=PT-BR
style='mso-ansi-language:PT-BR'> cessam as seis bases dos sentidos’ ... ’Da
cessação da consciência, cessa a mentalidade-materialidade (nome e forma)</span><span
lang=PT-BR style='mso-ansi-language:PT-BR'>’ ... ’Da cessação das formações volitivas,
cessa a consciência.’)<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Musila,
meu amigo, colocando de lado a fé, colocando de lado a preferência, colocando
de lado a tradição, colocando de lado a razão, colocando de lado as
idéias, você verdadeiramente possui conhecimento pessoal assim: ‘Da cessação da
ignorância, cessam as formações volitivas’?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
Pavittha, meu amigo. Colocando de lado a fé ... preferência ... tradição ... razão ... as idéias, eu verdadeiramente possuo conhecimento pessoal assim: ‘Da
cessação da ignorância, cessam as formações volitivas.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Musila,
meu amigo, colocando de lado a fé, colocando de lado a preferência, colocando
de lado a tradição, colocando de lado a razão, colocando de lado as
idéias, você verdadeiramente possui conhecimento pessoal assim: ‘A cessação do
ser/existir é a libertação’?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
Pavittha, meu amigo. Colocando de lado a fé ... preferência ... tradição ... razão ... as idéias, eu verdadeiramente possuo conhecimento pessoal assim: ‘A
cessação do ser/existir é a libertação.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
venerável Musila, você é um arahant cujas impurezas foram destruídas.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isso
foi dito, o venerável Musila permaneceu em silêncio. [<a href="#N1">1</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Então o venerável Narada disse, “Pavittha meu amigo, seria bom se você
dirigisse a mim essa pergunta. Faça a pergunta que eu a responderei.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então
farei essa pergunta ao venerável Narada. Eu perguntarei e que ele me responda.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(O venerável
Pavittha faz as mesmas perguntas ao venerável Narada que dá as mesmas respostas que
o venerável Musila.)<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
venerável Narada, você é um arahant cujas impurezas foram destruídas.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Meu amigo, embora eu tenha visto da forma apropriada, com correta
sabedoria, da maneira como na verdade é, que ‘A cessação do ser/existir é a
libertação,’ ainda não sou um arahant cujas impurezas foram destruídas. [<a
href="#N2">2</a>] <a name=R2></a>É como se houvesse um poço, sem uma corda e
sem um balde, numa estrada no deserto. Um homem surgiria subjugado pelo calor,
oprimido pelo calor, exausto, desidratado e com sede. Ele olharia dentro do
poço e se daria conta da ‘água’, mas ele não seria capaz de tocá-la com o seu
corpo. [<a href="#N3">3</a>] <a name=R3></a>Da mesma maneira, embora eu tenha
visto da forma apropriada, com correta sabedoria, da maneira como na verdade é,
que ‘A cessação do ser/existir é a libertação,’ ainda não sou um arahant cujas
impurezas foram destruídas.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isso
foi dito, o venerável Ananda disse para o venerável Pavittha, “Ao falar dessa forma,
amigo Pavittha, o que você tem a dizer sobre o venerável Narada?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando o
venerável Narada fala desse modo, amigo Ananda, eu não tenho nada a dizer sobre o
venerável Narada, exceto que ele é habilidoso e admirável.”<o:p></o:p></span></p>

