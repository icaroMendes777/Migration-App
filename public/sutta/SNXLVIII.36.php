<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Notas:<o:p></o:p></span></b></p>



<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] ‘Corporal’ significa baseado na sensibilidade corporal; ‘conforto’ é
sinônimo de prazer e significa doce, <i>(madhura). </i>[<a href="#R1">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[2] De acordo com o Abhidhamma, toda sensação corporal, isto é, uma
sensação que nasça através da sensibilidade corporal, é prazerosa ou dolorosa;
não há sensação neutra baseada na sensibilidade corporal. Por conseguinte, o
comentário explica a equanimidade corporal como a sensação baseada nos outros
quatro sentidos, o olho, etc. A palavra <i>upekha </i>traduzida como
equanimidade possui duas principais conotações. Com relação à sensação, a
conotação é de neutralidade, uma sensação que não é nem prazerosa, nem dolorosa.
Como uma qualidade mental, no entanto, a conotação é de neutralidade,
imparcialidade ou equilíbrio mental. Com este significado <i>upekha</i> aparece
como um dos quatro <i>brahmaviharas</i> (imparcialidade para
como todos os seres), como um dos sete fatores da iluminação, (equilíbrio
mental), e como uma qualidade da mente meditativa mencionada na fórmula dos
jhanas para o terceiro e quarto jhanas. [<a href="#R2">Retorna</a>]<o:p></o:p></span></p>





