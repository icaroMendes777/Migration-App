<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. Então, o Rei Pasenadi de Kosala foi até o Abençoado e depois de cumprimentá-lo
sentou a um lado. O Abençoado disse o seguinte:<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Grande
Rei, existem quatro tipos de pessoas que podem ser encontradas no mundo. Quais
quatro? Aquela que se dirige das trevas para as trevas, aquela que se dirige
das trevas para a luz, aquela que se dirige da luz para as trevas, aquela que
se dirige da luz para a luz. <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como,
grande rei, uma pessoa se dirige das trevas para as trevas? Aqui uma pessoa
renasceu numa família inferior – uma família de sudras, de cesteiros de bambu,
de caçadores, de consertadores de carruagens ou de lixeiros – numa família em que há
pouco para comer e beber e que sobrevive com dificuldades, onde comida e roupas
são obtidas com dificuldades; e ele é feio, antiestético, deformado, doente
crônico - peticego ou com as mãos deformadas ou coxo, ou paralítico. Ele não é
um daqueles que ganham comida, bebida, roupas e veículos; grinaldas, perfumes e
ungüentos; cama, moradia e lamparinas. Ele se dedica à conduta imprópria com o
corpo, linguagem e mente. Tendo feito isso, com a dissolução do corpo, após a
morte, ele renasce num estado de privação, num destino infeliz, nos reinos inferiores,
até mesmo no inferno.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Suponha,
grande rei, que um homem fosse da escuridão para a escuridão ou das trevas para
as trevas, ou do maculado para o maculado: essa pessoa, eu digo, é exatamente
igual. É dessa forma, grande rei, que uma pessoa se dirige das trevas para as
trevas.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como,
grande rei, uma pessoa se dirige das trevas para a luz? Aqui uma pessoa
renasceu numa família inferior ... onde comida e roupas são obtidas com
dificuldades; e ele é feio ... ou paralítico. Ele não é um daqueles que ganham
comida ... e lamparinas. Ele se dedica à conduta apropriada com o corpo,
linguagem e mente. Tendo feito isso, com a dissolução do corpo, após a morte, ele
renasce num destino feliz, até mesmo no paraíso. <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Suponha,
grande rei, que um homem subisse do chão para um tablado ou de um tablado para
um cavalo, ou de um cavalo para um elefante, ou de um elefante para um palácio:
essa pessoa, eu digo, é exatamente igual. É dessa forma, grande rei, que uma
pessoa se dirige das trevas para a luz.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como,
grande rei, uma pessoa se dirige da luz para as trevas? Aqui uma pessoa
renasceu numa família superior – uma família khattiya afluente, uma família
brâmane afluente, ou uma família de um chefe de família afluente – uma família
que é rica, com grande riqueza e posses, com ouro e prata em abundância, com
tesouros e mercadorias em abundância, com riqueza e grãos em abundância; e ele
é belo, atraente, possuindo beleza e complexão supremas. Ele é um daqueles que
ganham comida, bebida, roupas e veículos; grinaldas, perfumes e ungüentos;
cama, moradia e lamparinas. Ele se dedica à conduta imprópria com o corpo,
linguagem e mente. Tendo feito isso, com a dissolução do corpo, após a morte,
ele renasce num estado de privação, num destino infeliz, nos reinos inferiores, até mesmo
no inferno.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Suponha,
grande rei, que um homem descesse de um palácio para um elefante ou de um
elefante para um cavalo, ou de um cavalo para um tablado, ou de um tablado para
o chão, ou do chão para as trevas subterrâneas: essa pessoa, eu digo, é
exatamente igual. É dessa forma, grande rei, que uma pessoa se dirige da luz
para as trevas.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como,
grande rei, uma pessoa se dirige da luz para a luz? Aqui uma pessoa renasceu
numa família superior ... com riqueza e grãos em abundância; e ele é belo,
atraente, possuindo beleza e complexão supremas. Ele é um daqueles que ganham
comida ... e lamparinas. Ele se dedica à conduta apropriada com o corpo,
linguagem e mente. Tendo feito isso, com a dissolução do corpo, após a morte,
ele renasce num destino feliz, até mesmo no paraíso.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Suponha,
grande rei, que um homem atravessasse de um tablado para outro tablado ou de um
cavalo para outro cavalo, ou de um elefante para outro elefante, ou de um
palácio para outro palácio: essa pessoa, eu digo, é exatamente igual. É dessa
forma, grande rei, que uma pessoa se dirige da luz para a luz.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Essas,
grande rei, são os quatro tipos de pessoas que podem ser encontradas no mundo.<o:p></o:p></span></p>


<p class=Normal style='margin-left:1.75in'><span lang=PT-BR
style='mso-ansi-language:PT-BR'>(I)<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Ele, Oh rei, que é pobre, <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>carente de fé, mesquinho,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>avaro, com intenções ruins,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>idéias erradas, desrespeitoso,<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>que abusa e insulta os contemplativos,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>brâmanes e outros mendicantes;<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>um niilista, zombador, que impede<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>outrem de alimentar os mendicantes:<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Quando esse tal fulano morre, Oh rei,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>ele vai, senhor do povo,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>para o terrível inferno,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>dirigindo-se das trevas para as trevas.<o:p></o:p></span></p>


<p class=Normal style='margin-left:1.75in'><span lang=PT-BR
style='mso-ansi-language:PT-BR'>(II)<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Ele, Oh rei, que é pobre,<br>

