<p class=Normal><a name=N8></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[8] MA explica o kamma limitante, (<i>pamanakatam
kammam</i>), como o kamma pertencente à esfera sensual, (<i>kammavacara</i>).
