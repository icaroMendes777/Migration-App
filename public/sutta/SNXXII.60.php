<p class=Normal> Assim ouvi. Certa ocasião o Abençoado estava em Vesali na Grande Floresta no Salão com um pico na cumeeira. Então o Licchavi Mahali foi até o Abençoado e depois de cumprimentá-lo ele sentou a um lado e disse: </p>

<p class=Normal><a name=R1></a>"Venerável senhor, Purana Kassapa diz o seguinte: 'Não existem causas ou condições para a contaminação dos seres. Os seres são contaminados sem causas ou condições. Não há causas ou condições para a purificação dos seres. Os seres são purificados sem causas ou condições.' [<a href="#N1">1</a>] O que o Abençoado diz a respeito disso?" </p>

<p class=Normal>"Mahali, há causas ou condições para a contaminação dos seres; os seres são contaminados com causas ou condições. Há causas ou condições para a purificação dos seres; os seres são purificados com causas ou condições." </p>

<p class=Normal>"Mas, venerável senhor, qual é a causa ou condição para a contaminação dos seres? Como os seres são contaminados com causas ou condições?" </p>

<p class=Normal>"Mahali, se esta forma fosse exclusivamente sofrimento, imersa no sofrimento, impregnada de sofrimento, e se ela não estivesse também imersa no prazer, os seres não se enamorariam da forma. Mas porque a forma é prazerosa, impregnada de prazer, imersa no prazer, e não está apenas imersa no sofrimento, os seres se enamoram da forma. Enamorando-se da forma, os seres são cativados pela forma, e sendo cativados pela forma eles são contaminados. Mahali, essa é a causa ou condição para a contaminação dos seres; é assim que os seres são contaminados com causas ou condições. </p>

<p class=Normal>"Mahali, se esta sensação ... percepção ... formações volitivas ... consciência fosse exclusivamente sofrimento, imersa no sofrimento, impregnada de sofrimento, e se ela não estivesse também imersa no prazer, os seres não se enamorariam da consciência. Mas porque a consciência é prazerosa, impregnada de prazer, imersa no prazer, e não está apenas imersa no sofrimento, os seres se enamoram da consciência. Enamorando-se da consciência, os seres são cativados pela consciência, e sendo cativados pela consciência eles são contaminados. Mahali, essa é a causa ou condição para a contaminação dos seres; é assim que os seres são contaminados com causas ou condições. </p>

<p class=Normal>"Mas, venerável senhor, qual é a causa ou condição para a purificação dos seres? Como os seres são purificados com causas ou condições?" </p>

<p class=Normal>"Mahali, se esta forma fosse exclusivamente prazer, imersa no prazer, impregnada de prazer, e se ela não estivesse também imersa no sofrimento, os seres não experimentariam o desencantamento da forma. Mas porque a forma é sofrimento, impregnada de sofrimento, imersa no sofrimento, e não está apenas imersa no prazer, os seres experimentam o desencantamento da forma. Experimentando o desencantamento da forma, os seres se desapegam da forma, e desapegados da forma eles são purificados. Mahali, essa é a causa ou condição para a purificação dos seres; é assim que os seres são purificados com causas ou condições. </p>

<p class=Normal>"Mahali, se esta sensação ... percepção ... formações volitivas ... consciência fosse exclusivamente prazer, imersa no prazer, impregnada de prazer, e se ela não estivesse também imersa no sofrimento, os seres não experimentariam o desencantamento da consciência. Mas porque a consciência é sofrimento, impregnada de sofrimento, imersa no sofrimento, e não está apenas imersa no prazer, os seres experimentam o desencantamento da consciência. Experimentando o desencantamento da consciência, os seres se desapegam da consciência, e desapegados da consciência eles são purificados. Mahali, essa é a causa ou condição para a purificação dos seres; é assim que os seres são purificados com causas ou condições."
