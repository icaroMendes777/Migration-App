<p class=Normal> “Bhikkhus, tal como uma árvore desprovida de galhos e folhas, os seus brotos não irão amadurecer, nem a sua casca, nem o seu alburno, nem o seu cerne; assim também, naquele que carece de virtude, a concentração correta é forçosamente destruída ... o conhecimento e visão da libertação são forçosamente destruídos. </p>

<p class=Normal> “Bhikkhus, naquele que é virtuoso a concentração correta forçosamente prospera; quando há a concentração correta o conhecimento e visão de como as coisas na verdades são forçosamente prospera; quando há o conhecimento e visão de como as coisas na verdade são, o desencantamento e o desapego forçosamente prosperam; quando há o desencantamento e o desapego, o conhecimento e visão da libertação forçosamente prosperam. </p>

<p class=Normal> “Bhikkhus, tal como uma árvore que possui galhos e folhas, os seus brotos irão amadurecer, bem como a sua casca, o seu alburno, o seu cerne; assim também, naquele que é virtuoso a concentração forçosamente prospera; quando há a concentração correta o conhecimento e visão de como as coisas na verdades são forçosamente prospera ... o conhecimento e visão da libertação forçosamente prosperam.” </p>

