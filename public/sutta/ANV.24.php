<p class=Normal><a name=N2></a> [2]  “O conhecimento e visão de como as coisas na verdades são,” em pali: <i>yatha bhuta ñanadassana</i>.  Esta expressão aparece com freqüência e representa o primeiro estágio no processo de insight. <i>Yatha bhuta</i> tem sido traduzido “como as coisas na verdade são” e regra geral é traduzido de modo semelhante para o Inglês. Essa tradução no entanto apresenta algumas limitações no sentido de dar a impressão que existe algo especial a ser visto “nas coisas” e que a experiência de insight corresponde a ver esse algo especial, quando na verdade estamos mais interessados em ver o processo de fabricação que ocorre na mente e nesse sentido uma tradução mais esclarecedora para <i>yatha butha</i> seria “como as coisas vêm a ser” (através do processo de fabricação na mente). Em seguida a esse primeiro estágio de insight segue o desencantamento, (<i>nibbida</i>), depois o desapego, (<i>viraga</i>), e por fim culminando na completa libertação, (<i>vimutti</i>). [ <a href="#R2">Retorna</a>]</p>
