<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“E como, bhikkhus, um bhikkhu desenvolve e cultiva os quatro fundamentos
da atenção plena para ele declivar, tender e se inclinar na direção de Nibbana?
Neste caso, bhikkhus, um bhikkhu permanece contemplando o corpo como um corpo,
ardente, plenamente consciente e com atenção plena, tendo colocado de lado a
cobiça e o desprazer pelo mundo. Ele permanece contemplando as sensações como
sensações ... mente como mente ... objetos mentais como objetos mentais,
ardente, plenamente consciente e com atenção plena, tendo colocado de lado a
cobiça e o desprazer pelo mundo. É dessa forma, bhikkhus, que um bhikkhu
desenvolve e cultiva os quatro fundamentos da atenção plena para ele declivar,
tender e se inclinar na direção de Nibbana.”<o:p></o:p></span></p>

