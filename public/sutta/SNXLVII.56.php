<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, assim como o rio Gânges decliva, tende e se inclina na
direção do oceano, um bhikkhu que desenvolve e cultiva os quatro fundamentos da
atenção plena também decliva, tende e se inclina na direção de Nibbana.[<a
