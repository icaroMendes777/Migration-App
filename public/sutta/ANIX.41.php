<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Em certa ocasião o Abençoado estava entre os Mallas em uma cidade
denominada Uruvelakappa. Então, ao
amanhecer, o Abençoado se vestiu e tomando a tigela e o manto externo, foi para
Uruvelakappa. Depois de haver esmolado em Uruvelakappa e de haver retornado,
após a refeição ele disse para o Venerável Ananda, “Fique aqui,
Ananda, enquanto que eu irei passar o resto do dia na Grande Floresta.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim, senhor,” o Venerável
Ananda respondeu. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o Abençoado foi para
a Grande Floresta e sentou ao pé de uma certa árvore para passar o resto do
dia. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o chefe de família
Tapussa foi até o Venerável Ananda e ao chegar depois de
cumprimentá-lo, sentou a um lado e disse para o Venerável Ananda: “Venerável Ananda, nós que somos chefes de família nos entregamos aos prazeres dos sentidos,
nos deliciamos com os prazeres dos sentidos, desfrutamos dos prazeres dos sentidos, nos
alegramos com os prazeres dos sentidos. Para nós - que nos entregamos aos prazeres
dos sentidos, nos deliciamos com os prazeres dos sentidos, desfrutamos dos prazeres
dos sentidos, nos alegramos com os prazeres dos sentidos<span style="mso-spacerun:
yes">  </span>- a renúncia é como um total declínio. No entanto eu ouvi que
nesta doutrina e disciplina os corações dos bhikkhus jovens ficam excitados com
a renúncia, ganham confiança, decididos e firmes, vendo-a como estar em paz.
Então é exatamente nesse ponto que essa doutrina e disciplina é contrária à
grande massa das pessoas: isto é, (essa questão da) renúncia.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Isso requer uma conversa
com o Abençoado, chefe de família. Vamos até o Abençoado e ao chegar
reportaremos este assunto. Da forma como ele nos explicar, assim nós deveremos
nos recordar.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim, senhor,” o
chefe de família Tapussa respondeu ao Venerável Ananda. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o Venerável Ananda,
juntamente com Tapussa foram até o Abençoado e ambos o
cumprimentaram e sentaram a um lado e o Venerável Ananda disse para o Abençoado:
“Tapussa disse, ‘Venerável Ananda, nós que
somos chefes de família nos entregamos aos prazeres dos sentidos, nos deliciamos
com os prazeres dos sentidos, desfrutamos dos prazeres dos sentidos, nos alegramos com
os prazeres dos sentidos. Para nós - que nos entregamos aos prazeres dos sentidos, nos
deliciamos com os prazeres dos sentidos, desfrutamos dos prazeres dos sentidos, nos
alegramos com os prazeres dos sentidos<span style="mso-spacerun: yes">  </span>- a
renúncia é como um total declínio. No entanto eu ouvi que nesta doutrina e
disciplina os corações dos bhikkhus jovens ficam excitados com a renúncia,
ganham confiança, decididos e firmes, vendo-a como estar em paz. Então é
exatamente nesse ponto que essa doutrina e disciplina é contrária à grande
massa das pessoas: isto é, (essa questão da) renúncia.’” </span><span
lang=PT-BR style='mso-bidi-font-size:10.0pt;mso-ansi-language:PT-BR'><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Assim
é, Ananda. Assim é. Até mesmo eu, antes da minha iluminação, quando eu ainda era
um Bodisatva não desperto, pensava: ‘A renúncia é boa. O isolamento é bom.’
Porém o meu coração não ficava excitado com a renúncia, não ganhava confiança,
decisão e firmeza, vendo-a como estar em paz. O pensamento me ocorreu: ‘Qual é
a causa, qual é a razão, porque meu coração<span style="mso-spacerun: yes"> 
</span>não fica excitado com a renúncia, não ganha confiança, decisão e
firmeza, vendo-a como estar em paz?’ Então o pensamento me ocorreu: ‘Eu não vi
as desvantagens dos prazeres dos sentidos; eu não insisti (nesse tema). Eu não
compreendi as recompensas da renúncia; eu não me familiarizei com isso. Essa é
a razão porque meu coração não se excitava com a renúncia, não ganhava
confiança, decisão e firmeza, vendo-a como estar em paz.’ <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>[1]
“Então o pensamento me ocorreu: ‘Se, tendo visto as desvantagens dos prazeres
dos sentidos, eu insistisse nesse tema; e se, tendo entendido as recompensas da
renúncia, eu me familiarizasse com isso, existiria a possibilidade de que meu
coração ficasse excitado com a renúncia, ganhasse confiança, decisão e firmeza,
vendo-a como estar em paz.’ <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Dessa forma mais tarde,
tendo visto a desvantagem dos prazeres dos sentidos, eu insisti nesse tema; tendo
compreendido as recompensas da renúncia, eu me familiarizei com isso. Meu
coração ficou excitado com a renúncia, ganhou confiança, decisão e firmeza,
vendo-a como estar em paz. Então afastado dos prazeres sensuais, afastado das qualidades não hábeis, entrei e permaneci no primeiro jhana, que é caracterizado pelo pensamento aplicado e sustentado, com o êxtase e felicidade nascidos do afastamento. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Enquanto assim permanecia,
fui molestado com a atenção para as percepções que dizem respeito ao pensamento aplicado e sustentado. </span>Isso foi uma aflição para mim. <span lang=PT-BR
style='mso-ansi-language:PT-BR'>Tal como a dor surge como uma aflição para uma
pessoa saudável, assim também a atenção para as percepções que dizem respeito
ao pensamento aplicado e sustentado foi para mim uma aflição. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>[2] “O pensamento me
ocorreu: ‘E se, abandonando o pensamento aplicado e sustentado, eu entrasse e
permanecesse no segundo jhana, que é caracterizado pela segurança interna e perfeita unicidade da mente, sem o pensamento aplicado e sustentado, com o êxtase e felicidade nascidos da concentração.’ Mas o meu coração não ficou excitado em estar sem o
pensamento aplicado e sustentado, não ganhou confiança, decisão e firmeza,
vendo-o como estar em paz. O pensamento me ocorreu: ‘Qual é a causa, qual é a
razão, porque meu coração não fica excitado em estar sem o pensamento aplicado
e sustentado, não ganha confiança, decisão e firmeza, vendo-o como estar em
paz?’ Então o pensamento me ocorreu: ‘Eu não vi as desvantagens do pensamento
aplicado e sustentado; eu não insisti (nesse tema). Eu não compreendi as
recompensas em estar sem o pensamento aplicado e sustentado; eu não me
familiarizei com isso. Essa é a razão porque meu coração não se excitava em
estar sem o pensamento aplicado e sustentado, não ganhava confiança, decisão e
firmeza, vendo-o como estar em paz.' <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então o pensamento me
ocorreu: ‘Se, tendo visto as desvantagens do pensamento aplicado e sustentado,
eu insistisse nesse tema; e se, tendo compreendido as recompensas em estar sem
o pensamento aplicado e sustentado, eu me familiarizasse com isso, existiria a
possibilidade de que meu coração ficasse excitado em estar sem o pensamento
aplicado e sustentado, ganhasse confiança, decisão e firmeza, vendo-o como
estar em paz.’ <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Dessa forma mais tarde,
tendo visto a desvantagem do pensamento aplicado e sustentado, eu insisti nesse
tema; tendo compreendido as recompensas em estar sem o pensamento aplicado e sustentado,
eu me familiarizei com isso. Meu coração ficou excitado em estar sem o
pensamento aplicado e sustentado, ganhou confiança, decisão e firmeza, vendo-o
como estar em paz. Então abandonando o pensamento aplicado e sustentado, entrei e permaneci no segundo jhana, que é caracterizado pela segurança interna e perfeita unicidade da mente, sem o pensamento aplicado e sustentado, com o êxtase e felicidade nascidos da concentração. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Enquanto assim permanecia,
fui molestado com a atenção para as percepções que dizem respeito ao êxtase. Isso foi uma aflição para mim. Tal como a dor surge como
uma aflição para uma pessoa saudável, assim também a atenção para as percepções
que dizem respeito ao êxtase foi para mim uma
aflição. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>[3]”O pensamento me
ocorreu: ‘E se, abandonando o êxtase, eu entrasse e permanecesse no terceiro jhana que é caracterizado pela felicidade sem o êxtase, acompanhada pela atenção plena, plena consciência e equanimidade, acerca do qual os nobres declaram: “Ele permanece numa estada feliz, equânime e plenamente atento”?’ Mas o meu coração não ficou excitado em estar sem o êxtase, não
ganhou confiança, decisão e firmeza, vendo-o como estar em paz... Dessa forma
mais tarde, tendo visto a desvantagem do êxtase, eu insisti nesse tema; tendo
compreendido as recompensas em estar sem o êxtase, eu me familiarizei com isso.
Meu coração ficou excitado em estar sem o êxtase, ganhou confiança, decisão e
firmeza, vendo-o como estar em paz. Abandonando o êxtase, eu entrei e permaneci no terceiro jhana que é caracterizado pela felicidade sem o êxtase, acompanhada pela atenção plena, plena consciência e equanimidade, acerca do qual os nobres declaram: ‘Ele permanece numa estada feliz, equânime e plenamente atento’.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Enquanto assim permanecia,
fui molestado com a atenção para as percepções que dizem respeito à felicidade.
Isso foi uma aflição para mim. Tal como a dor surge como uma aflição para uma
pessoa saudável, assim também a atenção para as percepções que dizem respeito
à felicidade foi para mim uma aflição. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>[4] “O pensamento me
ocorreu: ‘E se, com o completo desaparecimento da felicidade, eu entrasse e permanecesse no quarto jhana, que possui nem felicidade nem sofrimento, com a atenção plena e a equanimidade purificadas?’ Mas o meu coração não ficou excitado em estar sem a felicidade, não ganhou confiança, decisão e firmeza, vendo-o como
estar em paz... Dessa forma mais tarde, tendo visto a desvantagem da felicidade, eu insisti nesse tema; tendo compreendido as recompensas da nem
felicidade nem sofrimento, eu me familiarizei com isso. Meu coração ficou
excitado com a nem felicidade nem sofrimento, ganhou confiança, decisão e
firmeza, vendo-a como o estar em paz. Com o completo desaparecimento da felicidade, eu entrei e
permaneci no quarto jhana, que possui nem felicidade nem sofrimento, com a atenção plena e a equanimidade purificadas. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Enquanto assim permanecia,
fui molestado com a atenção para as percepções que dizem respeito à
equanimidade. Isso foi uma aflição para mim. Tal como a dor surge como uma
aflição para uma pessoa saudável, assim também a atenção para as percepções que
dizem respeito à equanimidade foi para mim uma aflição. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>[5] “O pensamento me
ocorreu: ‘E se, com a completa superação das percepções da forma, com o
desaparecimento das percepções do contato sensorial, sem dar atenção às
percepções da diversidade, consciente de que o ‘espaço é infinito,’ eu entrasse e
permanecesse na base do espaço infinito?’ Mas o meu coração não ficou excitado
com a em base do espaço infinito, não ganhou confiança, decisão e firmeza,
vendo-a como estar em paz... Dessa forma mais tarde, tendo visto a desvantagem
das formas, eu insisti nesse tema; tendo compreendido as recompensas da base do
espaço infinito, eu me familiarizei com isso. Meu coração ficou excitado com a
base do espaço infinito, ganhou confiança, decisão e firmeza, vendo-a como
estar em paz com a completa superação das percepções da forma. Com o
desaparecimento das percepções do contato sensorial, sem dar atenção às
percepções da diversidade, consciente de que o ‘espaço é infinito,’ eu entrei e permaneci
na base do espaço infinito. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Enquanto assim permanecia,
fui molestado com a atenção para as percepções que dizem respeito ao espaço infinito.
Isso foi uma aflição para mim. Tal como a dor surge como uma aflição para uma
pessoa saudável, assim também a atenção para as percepções que dizem respeito
ao espaço infinito foi para mim uma aflição. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>[6] “O pensamento me
ocorreu: ‘E se, com a completa superação da base do espaço infinito, consciente
de que a ‘consciência é infinita,’ eu entrasse e permanecesse na base da
consciência infinita?’ Mas o meu coração não ficou excitado com a em base da
consciência infinita, não ganhou confiança, decisão e firmeza, vendo-a como
estar em paz... Dessa forma mais tarde, tendo visto a desvantagem da base do
espaço infinito, eu insisti nesse tema; tendo compreendido as recompensas da
consciência infinita, eu me familiarizei com isso. Meu coração ficou excitado
com a base da consciência infinita, ganhou confiança, decisão e firmeza,
vendo-a como estar em paz. Com a completa superação da base do espaço infinito,
consciente de que a ‘consciência é infinita,' eu entrei e permaneci na base da
consciência infinita. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Enquanto assim permanecia,
fui molestado com a atenção para as percepções que dizem respeito à consciência
infinita. Isso foi uma aflição para mim. Tal como a dor surge como uma aflição
para uma pessoa saudável, assim também a atenção para as percepções que dizem
respeito à consciência
infinita foi para mim uma aflição. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>[7] “O pensamento me
ocorreu: ‘E se, com a completa superação da base da consciência infinita,
consciente de que ‘não há nada,' eu entrasse e permanecesse na base do nada?'
Mas o meu coração não ficou excitado com a base do nada, não ganhou confiança,
decisão e firmeza, vendo-a como estar em paz... Dessa forma mais tarde, tendo
visto a desvantagem da base da consciência infinita, eu insisti nesse tema;
tendo compreendido as recompensas da base do nada, eu me familiarizei com isso.
Meu coração ficou excitado com a base do nada, ganhou confiança, decisão e
firmeza, vendo-a como estar em paz. Com a completa superação da base da
consciência infinita, consciente de que ‘não há nada,' eu entrei e permaneci na base do
nada. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Enquanto assim permanecia,
fui molestado com a atenção para as percepções que dizem respeito à base do nada. Isso foi uma aflição para mim. Tal como a dor surge como uma aflição
para uma pessoa saudável, assim também a atenção para as percepções que dizem
respeito à base do nada foi para mim uma aflição. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>[8] ] “O pensamento me
ocorreu: ‘E se, com a completa superação da base do nada, eu entrasse e
permanecesse na base da nem percepção, nem não percepção?' Mas o meu coração
não ficou excitado com a base da nem percepção, nem não percepção, não ganhou
confiança, decisão e firmeza, vendo-a como estar em paz... Dessa forma mais
tarde, tendo visto a desvantagem da base do nada, eu insisti nesse tema; tendo
compreendido as recompensas da base da nem percepção, nem não percepção, eu me
familiarizei com isso. Meu coração ficou excitado com a base da nem percepção,
nem não percepção, ganhou confiança, decisão e firmeza, vendo-a como estar em
paz. Com a completa superação da base do nada, eu entrei e permaneci na base da
nem percepção, nem não percepção. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Enquanto assim permanecia,
fui molestado com a atenção para as percepções que dizem respeito à base da
nem percepção, nem não percepção. Isso foi uma aflição para mim. Tal como a dor surge como uma aflição para
uma pessoa saudável, assim também a atenção para as percepções que dizem
respeito à base da
nem percepção, nem não percepção. foi para mim uma aflição. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>[9] &quot;“O pensamento me
ocorreu: ‘E se, com a completa superação da base da nem percepção, nem não
percepção, eu entrasse e permanecesse na cessação da percepção e sensação?’ Mas
o meu coração não ficou excitado com a cessação da percepção e sensação, não
ganhou confiança, decisão e firmeza, vendo-a como estar em paz. O pensamento me
ocorreu: ‘Qual é a causa, qual é a razão, porque meu coração não fica excitado
com a cessação da percepção e sensação, não ganha confiança, decisão e firmeza,
vendo-a como estar em paz.?’ Então o pensamento me ocorreu: ‘Eu não vi as
desvantagens da base da nem percepção, nem não percepção; eu não insisti (nesse
tema). Eu não compreendi as recompensas na cessação da percepção e sensação; eu
não me familiarizei com isso. Essa é a razão porque meu coração não se excitava
com a cessação da percepção e sensação, não ganhava confiança, decisão, ou
firmeza, vendo-a como estar em paz.'<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então o pensamento me
ocorreu: ‘Se, tendo visto as desvantagens da base da nem percepção, nem não
percepção, eu insistisse nesse tema; e se, tendo compreendido as recompensas da
cessação da percepção e sensação, eu me familiarizasse com isso, existiria a
possibilidade de que meu coração ficasse excitado com a cessação da percepção e
sensação, decisão e firmeza, vendo-a como estar em paz.’ <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Dessa forma mais tarde,
tendo visto a desvantagem da base da nem percepção, nem não percepção, eu
insisti nesse tema; tendo compreendido as recompensas da cessação da percepção
e sensação, eu me familiarizei com isso. Meu coração ficou excitado com a
cessação da percepção e sensação, ganhou confiança, decisão e firmeza, vendo-a
como estar em paz. Com a completa superação da base da nem percepção, nem não
percepção, eu entrei e permaneci na cessação da percepção e sensação. E eu vi
através da sabedoria, que as impurezas foram destruídas. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ananda, enquanto eu não
havia alcançado e emergido dessas nove realizações sucessivas em seqüência para
frente e para trás, eu não reivindiquei ter despertado para a insuperável
perfeita iluminação neste mundo com os seus devas, maras e brahmas, esta
população com seus contemplativos e brâmanes, seus príncipes e povo. Mas assim
que alcancei e emergi dessas nove realizações sucessivas em seqüência para
frente e para trás, então reivindiquei ter despertado para a insuperável
perfeita iluminação neste mundo ... seus príncipes e povo. Surgiram em mim a
visão clara e a sabedoria: ‘Minha libertação é inabalável. Este é o último
nascimento. Não haverá mais vir a ser a nenhum estado.”<o:p></o:p></span></p>

