<p class=Normal style='margin-left:1.0in'>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim ouvi.
Em certa ocasião o Abençoado estava com os Sakyas em Kapilavatthu, no Parque de
Nigrodha.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. Agora,
naquela ocasião um novo salão para assembléias havia sido construído<span
style='mso-spacerun:yes'>  </span>recentemente para os Sakyas de Kapilavatthu e
ele ainda não havia sido habitado de modo nenhum por nenhum contemplativo ou
brâmane ou ser humano. Então os Sakyas de Kapilavatthu foram até o Abençoado.
Depois de cumprimentá-lo eles sentaram a um lado e disseram:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, um novo salão para assembléias foi recentemente construído para os
Sakyas de Kapilavatthu e ele ainda não foi habitado de modo nenhum por nenhum
contemplativo ou brâmane ou ser humano. Venerável senhor, que o Abençoado seja
o primeiro a usá-lo. Depois que o Abençoado tiver primeiro usado o salão, então
os Sakyas de Kapilavatthu irão usá-lo. Isso será para a felicidade e bem-estar
deles por muito tempo.” [<a href="#N1">1</a>] <o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>3. O Abençoado concordou em silêncio. Então, quando eles viram que ele
havia concordado, eles levantaram dos seus assentos e depois de homenagear o
Abençoado, mantendo-o à sua direita, foram até o salão de assembléias. Eles
cobriram o salão completamente com coberturas e prepararam assentos, e eles
arranjaram um grande jarro com água e penduraram uma lamparina de azeite. Então
eles foram até o Abençoado e depois de cumprimentá-lo, ficaram em pé a um lado
e disseram:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, o salão de assembléias foi coberto completamente com coberturas e os
assentos foram preparados, um grande jarro com água foi arranjado e uma lamparina
de azeite foi pendurada. Agora é o momento para o Abençoado fazer aquilo que
julgar adequado.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. Então o
Abençoado se vestiu e tomando a sua tigela e o manto externo, foi com a Sangha
dos bhikkhus até o salão de assembléias. Ao chegar, ele lavou os pés e depois
entrou no salão e sentou próximo à coluna central olhando para o leste. E os
bhikkhus lavaram os pés e depois entraram no salão e sentaram na parede do lado
oeste olhando para o leste, com o Abençoado à sua frente. E os Sakyas de
Kapilavatthu lavaram os pés e entraram no salão e sentaram na parede do lado
leste olhando para o oeste, com o Abençoado à sua frente.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. Então,
depois do Abençoado ter instruído, motivado, estimulado e encorajado os Sakyas
de Kapilavatthu com um discurso do Dhamma, durante a maior parte da noite, ele
disse para o venerável Ananda:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ananda,
fale aos Sakyas de Kapilavatthu sobre o discípulo no treinamento superior que
entrou no caminho. [<a href="#N2">2</a>] <a name=R2></a>As minhas costas estão
me incomodando. Eu irei descansá-las.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
venerável senhor,” o venerável Ananda respondeu.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, o
Abençoado dobrou em quatro o seu manto feito de retalhos e deitou no seu lado
direito, na postura do leão, com um pé sobre o outro, atento e plenamente consciente,
após anotar na mente o horário para levantar.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. Então, o
venerável Ananda se dirigiu ao Sakya Mahanama da seguinte forma: <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mahanama,
aqui um nobre discípulo é virtuoso, guarda as portas dos meios dos sentidos, é
moderado na alimentação e dedicado à vigilância; ele possui sete boas
qualidades; e ele é aquele que obtém de acordo com a sua vontade, sem problemas
ou dificuldades, os quatro jhanas que constituem a mente superior e que
proporcionam uma estada prazerosa aqui e agora. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. “E como
um nobre discípulo é virtuoso? Aqui um nobre discípulo é virtuoso, contido
pelas regras do Patimokkha, perfeito na conduta e na sua esfera de atividades,
temendo a menor falha, ele treina adotando os preceitos de virtude. Assim é
como um nobre discípulo é virtuoso.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. “E como
um nobre discípulo guarda as portas dos meios dos sentidos? Ao ver uma forma
com o olho, um nobre discípulo não se agarra aos seus sinais ou detalhes. Visto
que, se permanecer com a faculdade do olho descuidada, ele será tomado pelos estados
ruins e prejudiciais de cobiça e tristeza. Ele pratica a contenção, ele protege
a faculdade do olho, ele se empenha na contenção da faculdade do olho. Ao ouvir
um som com o ouvido ... Ao cheirar um aroma com o nariz ... Ao saborear um sabor
com a língua ... Ao tocar um tangível com o corpo ... Ao conscientizar um objeto
mental<span style='mso-spacerun:yes'>  </span>com a<span
style='mso-spacerun:yes'>  </span>mente, ele não se agarra aos seus sinais ou
detalhes. Visto que, se permanecer com a faculdade da mente descuidada, ele
será tomado pelos estados ruins e prejudiciais de cobiça e tristeza. Ele pratica
a contenção, ele protege a faculdade da mente, ele se empenha na contenção da
faculdade da mente. Assim é como um nobre discípulo guarda as portas dos meios
dos sentidos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. “E como
um nobre discípulo é moderado na alimentação? Aqui, um nobre discípulo reflete
de maneira sábia, o alimento não deve ser tomado como forma de diversão ou para
embriaguez, tampouco com o objetivo de embelezamento e para ser mais atraente,
mas somente com o propósito de manter a resistência e continuidade desse corpo,
como forma de dar um fim ao desconforto e para auxiliar a vida santa.
Considerando: ‘Dessa forma darei um fim às antigas sensações, (de fome), sem
despertar novas sensações, (de comida em excesso), e serei saudável e sem culpa e
viverei em comodidade.’ Assim é como um nobre discípulo é moderado na
alimentação.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. “E como
um nobre discípulo é dedicado à vigilância? Aqui, durante o dia, um nobre
discípulo, enquanto estiver caminhando para lá e para cá e sentado, purifica a
sua mente dos estados obstrutivos. Na primeira vigília da noite, enquanto
estiver caminhando para lá e para cá e sentado, ele purifica a sua mente dos
estados obstrutivos. Na segunda vigília da noite, ele se deita para dormir no
seu lado direito, na postura do leão, com um pé sobre o outro, atento e
plenamente consciente, após anotar na sua mente o horário para se levantar.
Após levantar-se, na terceira vigília da noite, enquanto estiver caminhando
para cá e para lá e sentado, ele purifica a sua mente dos estados obstrutivos.
Assim é como um nobre discípulo é dedicado à vigilância. <o:p></o:p></span></p>



<p class=Normal><a name=T11></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>11. “E como um nobre discípulo possui sete boas qualidades? Aqui um
nobre discípulo tem fé; ele deposita a sua fé na iluminação do Tathagata assim:
‘O Abençoado é um arahant, perfeitamente iluminado, consumado no verdadeiro
conhecimento e conduta, bem-aventurado, conhecedor dos mundos, um líder
insuperável de pessoas preparadas para serem treinadas, mestre de devas e
humanos, desperto, sublime.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. “Ele
tem vergonha de cometer transgressões; ele se envergonha do comportamento
impróprio através do corpo, linguagem e mente, se envergonha de praticar atos
ruins e prejudiciais.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. “Ele
tem temor de cometer transgressões; ele teme o comportamento impróprio através
do corpo, linguagem e mente, teme praticar atos ruins e prejudiciais. [<a
href="#N3">3</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>14. “Ele aprendeu muito, se recorda daquilo que aprendeu e consolida
aquilo que<span style='mso-spacerun:yes'>  </span>aprendeu. Aqueles
ensinamentos que são admiráveis no início, admiráveis no meio, admiráveis no
final, com o correto significado e fraseado e que revelam uma vida santa que
é completamente perfeita e imaculada – esses ensinamentos ele compreendeu
bem, se recorda, dominou com a linguagem, investigou com a mente e penetrou
corretamente com o entendimento.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. “Ele
tem energia para abandonar os estados prejudiciais e adotar os estados
benéficos; ele é decidido, firme no esforço, não é negligente no
desenvolvimento de estados benéficos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16. “Ele
possui atenção plena, estando dotado com a atenção plena discriminatória superior, 
de modo que ele tem boa memória e se recorda daquilo que foi feito há
muito tempo e do que foi dito há muito tempo. [<a href="#N4">4</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>17. “Ele é sábio; ele possui a sabedoria com relação à origem e a
cessação que é nobre e profunda e que conduz à completa destruição do
sofrimento. [<a href="#N5">5</a>] <a name=R5></a>Assim é como um nobre
discípulo possui sete boas qualidades.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18. “E como
um nobre discípulo é aquele que obtém de acordo com a sua vontade, sem
problemas ou dificuldades, os quatro jhanas que constituem a mente superior e
que proporcionam uma estada prazerosa aqui e agora? Aqui, um nobre discípulo afastado dos prazeres
sensuais, afastado das qualidades não hábeis,  entra e
permanece no primeiro jhana ... Abandonando o pensamento aplicado e sustentado,
ele entra e permanece no segundo jhana ... Abandonando o êxtase ... ele
entra e permanece no terceiro jhana ... Com o completo desaparecimento da felicidade ... 
ele entra e permanece no quarto jhana, que possui nem felicidade nem sofrimento, com a atenção plena e a equanimidade purificadas.
Assim é como um nobre discípulo é aquele que obtém de acordo com a sua vontade,
sem problemas ou dificuldades, os quatro jhanas que constituem a mente superior
e que proporcionam uma estada prazerosa aqui e agora.<o:p></o:p></span></p>



<p class=Normal><a name=T19></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>19. “Quando um nobre discípulo é assim virtuoso, guarda as portas dos
meios dos sentidos, é moderado na alimentação, dedicado à vigilância, possui
sete boas qualidades, e obtém de acordo com a sua vontade, sem problemas ou dificuldades,
os quatro jhanas que constituem a mente superior e que proporcionam uma estada
prazerosa aqui e agora, ele é chamado aquele no treinamento superior que entrou
no caminho. O ovo dele está intato; ele é capaz de romper a casca, capaz de se
iluminar, capaz de alcançar a suprema segurança contra o cativeiro. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Suponham
que uma galinha tenha oito, dez ou doze ovos que ela cobriu corretamente,
aqueceu corretamente, incubou corretamente. [</span><a href="#N6"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>6</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>] <a name=R6></a>Mesmo que este desejo não lhe
ocorra, ‘Que as minhas crias rompam as cascas dos ovos com suas garras afiadas
ou bicos e saiam dos ovos com segurança!’, ainda assim os pintos são capazes de
romper as cascas dos ovos com suas garras afiadas ou bicos e sair dos ovos com
segurança. Da mesma forma, quando um nobre discípulo é assim virtuoso ... ele é
chamado aquele em treinamento superior que entrou no caminho. O ovo dele está
intato; ele é capaz de romper a casca, capaz de se iluminar, capaz de alcançar
a suprema segurança do cativeiro.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>20. “Tendo
obtido aquela mesma suprema atenção plena purificada devido à equanimidade, [<a
href="#N7">7</a>] <a name=R7></a>este nobre discípulo se recorda das suas
muitas vidas passadas ... (igual ao <a href="MN51.php#T24">MN 51, verso 24</a>)
... Assim ele se recorda das suas muitas vidas passadas nos seus modos e
detalhes. Este é o primeiro rompimento, como o dos pintos ao sair das suas
cascas.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>21. “Tendo
obtido aquela suprema atenção plena purificada devido à equanimidade, por meio
do olho divino, que é purificado e sobrepuja o humano, este nobre discípulo vê
seres falecendo e renascendo ... (igual ao <a href="MN51.php#T25">MN 51, verso
25</a>) ... ele compreende como os seres continuam de acordo com as suas ações.
Este é o segundo rompimento, como o dos pintos ao sair das suas cascas.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>22. “Tendo
obtido aquela suprema atenção plena purificada devido à equanimidade,
compreendendo por si mesmo com conhecimento direto, este nobre discípulo aqui e
agora entra e permanece na libertação da mente e na libertação através da
sabedoria, que são imaculadas, com a destruição de todas as impurezas.<span
style='mso-spacerun:yes'>  </span>Este é o terceiro rompimento, como o dos
pintos ao sair das suas cascas.[<a href="#N8">8</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R8></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>23. “Quando um nobre discípulo é virtuoso, isso é a conduta dele. Quando
ele guarda as portas dos meios dos sentidos, isso é a conduta dele. Quando ele
é moderado na alimentação, isso é a conduta dele. Quando ele é dedicado à
vigilância, isso é a conduta dele. Quando ele possui sete boas qualidades, isso
é a conduta dele. Quando ele obtém, de acordo com a sua vontade, sem problemas
ou dificuldades, os quatro jhanas que constituem a mente superior e que
proporcionam uma estada prazerosa aqui e agora, isso é a conduta dele. [<a
href="#N9">9</a>] <o:p></o:p></span></p>



<p class=Normal><a name=R9></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>24. “Quando ele se recorda das suas muitas vidas passadas ... nos seus
modos e detalhes, isso é o verdadeiro conhecimento dele. Quando,<span
style='mso-spacerun:yes'>  </span>por meio do olho divino ... ele vê seres
falecendo e renascendo, e compreende como os seres continuam de acordo com as
suas ações, isso é o verdadeiro conhecimento dele. Quando, compreendendo por si
mesmo com conhecimento direto, este nobre discípulo aqui e agora entra e
permanece na libertação da mente e na libertação através da sabedoria que são
imaculadas, com a destruição de todas as impurezas; isso é o verdadeiro
conhecimento dele.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>25. “Este
nobre discípulo se diz, portanto, perfeito no verdadeiro conhecimento, perfeito
em conduta, perfeito no verdadeiro conhecimento e conduta. Estes versos foram
recitados pelo Brahma Sanankumara:<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>‘Os khattiya são os melhores dentre as pessoas<br>
para aqueles cujo padrão é o clã,<br>
mas aquele com a conduta e o conhecimento consumados<br>
é o melhor dentre devas e humanos.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Agora,
esses versos foram bem recitados pelo Brahma Sanankumara, não mau recitados,
foram bem ditos e não mau ditos; eles possuem significado e não são desprovidos
de significado; e foram aprovados pelo Abençoado.” [<a href="#N10">10</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R10></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>26. Então o Abençoado se levantou e se dirigiu ao venerável Ananda
assim: “Muito bem, Ananda! É bom que você tenha falado aos Sakyas de
Kapilavatthu sobre o discípulo no treinamento superior que entrou no caminho.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o
que o venerável Ananda disse. O Mestre aprovou. Os Sakyas de Kapilavatthu
ficaram satisfeitos e contentes com as palavras do venerável Ananda.<o:p></o:p></span></p>

