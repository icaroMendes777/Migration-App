<p class=Normal>Assim ouvi. Em certa ocasião, o Abençoado estava nas redondezas de Pava, no Santuário Ajakalapaka, lugar frequentado pelo yakkha Ajakalapaka. Naquele ocasião, no meio da escuridão da noite, o Abençoado estava sentado em meditação ao ar livre enquanto que os devas faziam cair uma chuva esparsa.</p>



<p class=Normal>Então, Ajakalapaka - intencionando causar medo, terror e horripilação ao Abençoado - foi até ele e, ao chegar, não muito longe dele, três vezes gritou agitado e violentamente "Akkulo, pakkulo!", ameaçando-o: "Contemplativo, esta assombração é para você!”</p>



<p class=Normal>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p>



    <p class=Normal style='margin-left:.75in'>“É um brâmane aquele que<br> com relação à suas próprias qualidades cruzou para a outra margem.<br>

	Ele supera e transcende este yakkha e suas assombrações.”</p>

