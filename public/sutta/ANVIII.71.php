<p class=Normal> “Bhikkhus, um bhikkhu pode ter muita fé mas não ter virtude. Assim ele é deficiente em relação a esse fator. Ele deve completar esse fator, pensando, ‘Como posso ter muita fé e também ter virtude?’ Quando um bhikkhu tem ambos muita fé e virtude, ele está completo em relação a esse fator. </p>

 

<p class=Normal> “Um bhikkhu pode ter muita fé e ter virtude mas não ser estudado. Assim ele é deficiente em relação a esse fator. Ele deve completar esse fator, pensando, ‘Como posso ter muita fé e virtude e também ser estudado?’ Quando um bhikkhu tem muita fé, tem virtude e também é estudado, ele está completo em relação a esse fator. </p>

 

<p class=Normal> “Um bhikkhu pode ter muita fé, virtude e ser estudado, mas ele não é um bom orador do Dhamma ... ele não freqüenta assembléias ... ele não ensina com confiança o Dhamma para uma assembléia ... ele não obtém de acordo com a sua vontade, sem problemas ou dificuldades, os quatro jhanas que constituem a mente superior, e que proporcionam um estado prazeroso aqui e agora ... ele não é aquele que compreendendo por si mesmo com o conhecimento direto, aqui e agora, entra e permanece na libertação da mente e na libertação através da sabedoria que são imaculadas com a destruição de todas as impurezas. Assim ele é deficiente em relação a esse fator. Ele deve completar esse fator, pensando, ‘Como posso ter muita fé ... e compreendendo por mim mesmo com o conhecimento direto, aqui e agora entrar e permanecer na libertação da mente e na libertação através da sabedoria que são imaculadas com a destruição de todas as impurezas?’ </p>

 

<p class=Normal> “Quando um bhikkhu tem muita fé, virtude, é estudado, é um bom orador do Dhamma, freqüenta assembléias, ensina com confiança o Dhamma para uma assembléia, obtém de acordo com a sua vontade, sem problemas ou dificuldades, os quatro jhanas que constituem a mente superior, e que proporcionam um estado prazeroso aqui e agora, é aquele que compreendendo por si mesmo com o conhecimento direto, aqui e agora, entra e permanece na libertação da mente e na libertação através da sabedoria que são imaculadas com a destruição de todas as impurezas, então ele está completo em relação a todos esses fatores. </p>

 

<p class=Normal> “Um bhikkhu que possui essas oito qualidades inspira a todos à sua volta e é perfeito em todos os aspectos. </p>

 
