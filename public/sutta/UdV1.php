<p class=Normal> Em Savatthi. Agora, naquela ocasião o Rei Pasenadi de Kosala junto com a Rainha Mallika haviam subido até o terraço no topo do palácio. Então o Rei Pasenadi de Kosala disse para a Rainha Mallika: </p>


<p class=Normal> “Mallika, há alguém que você queira mais do que a você mesma?” [<a href="#N1">1</a>]</p>


<p class=Normal><a name=R1></a> “Não há ninguém, grande rei, que eu queira mais do que a mim mesma. Mas há alguém, grande rei, que você queira mais do que a você mesmo?" </p>


<p class=Normal> “Para mim também, Mallika, não há ninguém que eu queira mais do que a mim mesmo.” Então o Rei Pasenadi de Kosala desceu do palácio e foi até o Abençoado. Depois de cumprimentá-lo ele sentou a um lado e relatou a conversa com a Rainha Mallika. </p>


<p class=Normal> Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou: </p>

<p class=Normal style='margin-left:.75in'> “Tendo atravessado todos os quadrantes com a mente,<br>
ele não encontra em nenhum lugar alguém mais querido do que ele mesmo.<br>
Do mesmo modo, toda pessoa considera a si mesma como a mais querida;<br>
por conseguinte quem ama a si mesmo não deveria ferir os outros.”</p>


