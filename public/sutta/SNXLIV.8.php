<p class=Normal> Então o errante Vacchagotta foi até o Abençoado e ambos se cumprimentaram. Quando a conversa cortês e amigável havia terminado, ele sentou a um lado e disse: </p>

<p class=Normal> "Como é, Mestre Gotama, o mundo é eterno’?” </p>

<p class=Normal> “Vaccha, eu não declarei isso: o mundo é eterno.” </p>

<p class=Normal> “Nesse caso, o mundo não é eterno?” </p>

<p class=Normal> “Vaccha, eu não declarei isso: o mundo não é eterno." </p>

<p class=Normal> “Nesse caso, o mundo é finito?” </p>

<p class=Normal> “Vaccha, eu não declarei isso: o mundo é finito.” </p>

<p class=Normal> “Nesse caso, o mundo é infinito?” </p>

<p class=Normal> “Vaccha, eu não declarei isso: o mundo é infinito.” </p>

<p class=Normal> “Nesse caso, a alma e o corpo são a mesma coisa?” </p>

<p class=Normal> “Vaccha, eu não declarei isso: a alma e o corpo são a mesma coisa.” </p>

<p class=Normal> “Nesse caso, a alma é uma coisa e o corpo outra?” </p>

<p class=Normal> “Vaccha, eu não declarei isso: a alma é uma coisa e o corpo outra.” </p>

<p class=Normal> “Nesse caso, após a morte um Tathagata existe?” </p>

<p class=Normal> “Vaccha, eu não declarei isso: após a morte um Tathagata existe.” </p>

<p class=Normal> “Nesse caso, após a morte um Tathagata não existe’?” </p>

<p class=Normal> “Vaccha, eu não declarei isso: após a morte um Tathagata não existe.” </p>

<p class=Normal> “Nesse caso, após a morte um Tathagata tanto existe como não existe?” </p>

<p class=Normal> “Vaccha, eu não declarei isso: após a morte um Tathagata tanto existe como não existe.” </p>

<p class=Normal> “Nesse caso, após a morte um Tathagata nem existe nem não existe?” </p>

<p class=Normal> “Vaccha, eu não declarei isso: após a morte um Tathagata nem existe nem não existe.” </p>

<p class=Normal> “Mestre Gotama, qual é a causa e razão pela qual, quando os contemplativos de outras seitas são assim questionados, eles dão respostas como: 'o mundo é eterno' ...  'após a morte um Tathagata nem existe nem não existe'. Qual é a causa e razão pela qual, quando o Mestre Gotama é assim questionado ele não dá esse tipo de resposta? </p>

<p class=Normal>Vaccha, os errantes de outras seitas consideraram que a forma é o eu, ou o eu possuído de forma, ... ou a forma como estando no eu, ... ou o eu como estando na forma. Eles consideram que a sensação é o eu, ou o eu possuído de sensação, ... ou a sensação como estando no eu, ... ou o eu como estando na sensação. Eles consideram que a percepção seja o eu, ou o eu possuído de percepção, ... ou a percepção como estando no eu, ... ou o eu como estando na percepção. Eles consideram que as formações sejam o eu, ou o eu possuído de formações, ... ou as formações como estando no eu, ... ou o eu como estando nas formações. Eles consideram que a consciência seja o eu, ou o eu possuído de consciência, ... ou a consciência como estando no eu, ... ou o eu como estando na consciência. Portanto, quando são assim questionados, eles dão respostas como: 'o mundo é eterno' ...  'após a morte um Tathagata nem existe nem não existe'. Mas Vaccha, o Tathagata, um arahant perfeitamente iluminado não considera que a forma é o eu ... ou o eu como estando na consciência. Por conseguinte, quando o Tathagata é assim questionado ele não dá esse tipo de resposta." </p>


