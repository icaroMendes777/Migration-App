abandono. [<a href="#RA">Retorna</a>]<o:p></o:p></span></p>

<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[2] MA: “O caminho” são as cinco qualidades
começando pela fé junto com a aspiração. Aquele que tiver as cinco qualidades
sem a aspiração, ou a aspiração sem as qualidades, não terá uma destinação
determinada. A destinação só pode ser determinada quando ambos os fatores estão
presentes. [<a href="#R2">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[3] MA explica que há cinco tipos de
penetração: penetração da mente, isto é, conhecer os pensamentos dos seres por
toda a parte em mil mundos; penetração de uma kasina, isto é, estender a imagem
de uma kasina em mil mundos; penetração do olho divino, isto é, ver mil mundos
através do olho divino; penetração da luz, que é o mesmo que a penetração
anterior e penetração do corpo, isto é, estender a própria aura corporal em mil
mundos. [<a href="#R3">Retorna</a>]<o:p></o:p></span></p>


