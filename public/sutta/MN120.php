<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no
Parque de Anathapindika. Lá ele se dirigiu aos monges desta forma: “Bhikkhus” –
“Venerável Senhor,” eles responderam. O Abençoado disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.
“Bhikkhus, eu ensinarei para vocês o renascimento de acordo com as próprias
aspirações, [<a href="#N1">1</a>] <a name=R1></a>ouçam e prestem muita atenção
ao que vou dizer.” – “Sim, venerável senhor,” os bhikkhus responderam. O
Abençoado disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. “Aqui,
bhikkhus, um bhikkhu possui fé, virtude, aprendizado,
generosidade [</a><a href="#NA">A</a>]
<a name=RA></a>e sabedoria. Ele pensa: ‘Oh! Que com a dissolução do
corpo, depois da morte, eu possa renascer na companhia dos nobres <span
class=GramE>prósperos!</span>’ Ele fixa a mente nisso, estabelece isso,
desenvolve isso. Essa sua aspiração e fixação, assim desenvolvida e cultivada,
conduzem ao seu reaparecimento lá. Esse, bhikkhus, é o caminho, o modo que
conduz ao renascimento lá. [<a href="#N2">2</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>4-5. “Outra vez, um bhikkhu possui <span class=GramE>fé ...</span> e
sabedoria. Ele pensa: ‘Oh! Que com a dissolução do corpo, depois da morte, eu
possa renascer na companhia dos brâmanes prósperos!’... na companhia dos chefes
de família prósperos!’ Ele fixa a mente nisso ... Esse, bhikkhus, é o caminho,
o modo que conduz ao renascimento lá.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. “Outra
vez, um bhikkhu possui <span class=GramE>fé ...</span> e sabedoria. Ele ouve
que os devas dos Quatro Grandes Reis possuem vida longa, são belos e desfrutam
de muita felicidade. Ele pensa: ‘Oh! Que com a dissolução do corpo, depois da
morte, eu possa renascer na companhia dos devas dos Quatro Grandes Reis!’ Ele
fixa a mente nisso ... Esse, bhikkhus, é o caminho, o modo que conduz ao
renascimento lá.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7-11.
“Outra vez, um bhikkhu possui <span class=GramE>fé ...</span> e sabedoria. Ele
ouve que os devas do Trinta e Três ... os devas de Yama ... os devas de Tusita
... os devas que se deliciam com a criação ... os devas que exercem o poder
sobre a criação dos outros possuem vida longa, são belos, e desfrutam de muita
felicidade. Ele pensa: ‘Oh! Que com a dissolução do corpo, depois da morte, eu
possa renascer na companhia dos devas que exercem o poder sobre a criação dos
outros!’ Ele fixa a mente nisso ... Esse, bhikkhus, é o caminho, o modo que
conduz ao renascimento lá.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. “Outra
vez, um bhikkhu possui <span class=GramE>fé ...</span> e sabedoria. Ele ouve
que o Brahma de Mil possui vida longa, é belo e desfruta de muita felicidade.
Agora, o Brahma de Mil permanece determinado a penetrar um sistema cósmico com
mil mundos e permanece determinado a penetrar os seres que ali renasceram. [<a
href="#N3">3</a>] <a name=R3></a>Tal qual um homem com boa visão pode tomar com
a mão uma noz e examiná-la, da mesma forma o Brahma de Mil permanece
determinado a penetrar um sistema cósmico com mil [mundos], e permanece
determinado a penetrar os seres que ali renasceram. O bhikkhu pensa: ‘Oh! Que
com a dissolução do corpo, depois da morte, eu possa renascer na companhia do
Brahma de Mil!’ Ele fixa a mente nisso ... Esse, bhikkhus, é o caminho, o modo
que conduz ao renascimento lá.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13-16.
“Outra vez, um bhikkhu possui <span class=GramE>fé ...</span> e sabedoria. Ele
ouve que o Brahma de Dois Mil ... o Brahma de Três Mil ... o Brahma de Quatro
Mil ... o Brahma de Cinco Mil possui vida longa, é belo e desfruta de muita
felicidade. Agora, o Brahma de Cinco Mil permanece determinado a penetrar um
sistema cósmico com cinco mil mundos e permanece determinado a penetrar os
seres que ali renasceram. Tal qual um homem com boa visão pode tomar com a mão
cinco nozes e examiná-las, da mesma forma o Brahma de Cinco Mil permanece
determinado a penetrar um sistema cósmico com cinco mil [mundos] e permanece
determinado a penetrar os seres que ali renasceram. O bhikkhu pensa: ‘Oh! Que
com a dissolução do corpo, depois da morte, eu possa renascer na companhia do
Brahma de Cinco Mil!’ Ele fixa a mente nisso ... Esse, bhikkhus, é o caminho, o
modo que conduz ao renascimento lá.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17. “Outra
vez, um bhikkhu possui <span class=GramE>fé ...</span> e sabedoria. Ele ouve
que o Brahma de Dez Mil possui vida longa, é belo e desfruta de muita
felicidade. Agora, o Brahma de Dez Mil permanece determinado a penetrar um
sistema cósmico com dez mil mundos e permanece determinado a penetrar os seres
que ali renasceram. Tal qual uma bela pedra de berilo da mais pura água, com
oito facetas, bem lapidada, sobre um brocado vermelho, brilha, radia e cintila,
da mesma maneira o Brahma de Dez Mil permanece determinado a penetrar um
sistema cósmico com dez mil [mundos] e permanece determinado a penetrar os
seres que ali renasceram. O bhikkhu pensa: ‘Oh! Que com a dissolução do corpo,
depois da morte, eu possa renascer na companhia do Brahma de Dez Mil!’ Ele fixa
a mente nisso ... Esse, bhikkhus, é o caminho, o modo que conduz ao
renascimento lá.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18. “Outra
vez, um bhikkhu possui <span class=GramE>fé ...</span> e sabedoria. Ele ouve
que o Brahma de Cem Mil possui vida longa, é belo e desfruta de muita
felicidade. Agora, o Brahma de Cem Mil permanece determinado a penetrar um
sistema cósmico com cem mil mundos e permanece determinado a penetrar os seres
que ali renasceram. Tal qual um ornamento feito com ouro puro, trabalhado na
fornalha com habilidade por um ourives experto, sobre um brocado vermelho,
brilha, radia e cintila, da mesma maneira o Brahma de Cem Mil permanece
determinado a penetrar um sistema cósmico com cem mil [mundos] e<span
style='mso-spacerun:yes'>  </span>permanece determinado a penetrar os seres que
ali renasceram. O bhikkhu pensa: ‘Oh! Que com a dissolução do corpo, depois da
morte, eu possa renascer na companhia do Brahma de Cem Mil!’ Ele fixa a mente nisso
... Esse, bhikkhus, é o caminho, o modo que conduz ao renascimento lá.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>19-32.
“Outra vez, um bhikkhu possui <span class=GramE>fé ...</span> e sabedoria. Ele
ouve que os devas da Radiância [<a href="#N4">4</a>] <a name=R4></a>... os
devas da Radiância Limitada .... os devas da Radiância Imensurável ... os devas
que Emanam Radiância<span style='mso-spacerun:yes'>  </span>... os devas da
Glória ... os devas da Glória Limitada ... os devas da Glória Imensurável ...
os devas da Glória Refulgente ... os devas do Grande Fruto ... os devas de Aviha
... os devas de Atappa ... os devas de Sudassa ... os devas de Sudassi ... os
devas de Akanittha possuem vida longa, são belos e desfrutam de muita
felicidade. Ele pensa: ‘Oh! Que com a dissolução do corpo, depois da morte, eu
possa renascer na companhia dos devas Akanittha!’ Ele fixa a mente nisso ...
Esse, bhikkhus, é o caminho, o modo que conduz ao renascimento lá.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>33-36.
“Outra vez, um bhikkhu possui <span class=GramE>fé ...</span> e sabedoria. Ele
ouve que os devas da base do espaço infinito ... os devas da base da
consciência infinita ... os devas da base do nada ... os devas da base da nem
percepção, nem não percepção possuem vida longa, duram muito tempo e desfrutam
de muita felicidade. Ele pensa: ‘Oh! Que com a dissolução do corpo, depois da
morte, eu possa renascer na companhia dos devas da base da nem percepção, nem
não percepção!’ Ele fixa a mente nisso ... Esse, bhikkhus, é o caminho, o modo
que conduz ao renascimento lá.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>37. “Outra
vez, bhikkhus, um bhikkhu possui fé, virtude, aprendizado, generosidade e
sabedoria. Ele pensa: ‘Oh! Que realizando por mim mesmo através do conhecimento
direto, eu possa aqui e agora entrar e permanecer na libertação da mente e na
libertação através da sabedoria que são imaculadas com a destruição das impurezas!’ E
por ter realizado por si mesmo através do conhecimento direto, ele aqui e agora
entra e permanece na libertação da mente e na libertação através da sabedoria que são
imaculadas com a destruição de todas as impurezas. Bhikkhus, esse bhikkhu não
renasce em nenhum lugar de modo nenhum.” [<a href="#N5">5</a>]<o:p></o:p></span></p>



