<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[1] Embora <i>sankhara </i>tenha sido
regularmente traduzido como “formações,” neste caso parece que o contexto requer
uma interpretação distinta. MA explica inicialmente o significado de <i>sankharupapatti
</i><span style='mso-bidi-font-style:italic'>ou</span> como o reaparecimento,
(isto é, renascimento), das meras formações, não de um ser ou pessoa, ou o
reaparecimento dos agregados numa nova existência devido a uma formação
meritória de kamma. No entanto, em outros trechos, MA explica <i>sankhara</i>
através de <a name=PVW><i>patthana</i></a>, uma palavra que claramente
significa aspiração. [<a href="#R1">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=NA></a><span lang=PT-BR style='mso-ansi-language:
