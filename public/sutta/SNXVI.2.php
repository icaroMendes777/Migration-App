<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim ouvi.
Certa ocasião, o Venerável Mahakassapa e o Venerável Sariputta estavam em Benares no Parque do Gamo em Isipatana. Então, ao anoitecer, o Venerável
Sariputta saiu do seu isolamento e foi até o Venerável Mahakassapa e ambos se
cumprimentaram. Quando a conversa cortês e amigável havia terminado, ele sentou
a um lado e disse:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigo,
diz-se que aquele que não é ardente e que não teme a transgressão é incapaz de
se iluminar, incapaz de alcançar Nibbana, incapaz de alcançar a insuperável
segurança contra o cativeiro; mas aquele que é ardente e que teme a transgressão
é capaz de se iluminar, capaz de alcançar Nibbana, capaz de<span
style="mso-spacerun: yes">  </span>alcançar a insuperável segurança contra o
cativeiro.[<a href="#N1">1</a>]<a name=R1></a> Qual o significado disso, amigo?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui, amigo,
um bhikkhu não desperta o ardor através do pensamento: ‘Se os estados ruins e
prejudiciais que ainda não surgiram surgirem em mim, isso poderá resultar em
meu prejuízo’; ou através do pensamento: ‘Se os estados ruins e prejudiciais
que já surgiram em mim não forem abandonados, isso poderá resultar em meu
prejuízo’; ou através do pensamento: ‘Se os estados benéficos que ainda não
surgiram não surgirem em mim, isso poderá resultar em meu prejuízo’; ou através
do pensamento: ‘Se os estados benéficos que já surgiram em mim cessarem, isso
poderá resultar em meu prejuízo.’Assim, ele não é ardente.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como,
amigo, ele não teme a transgressão? Aqui, amigo, um bhikkhu não teme o
pensamento: ‘Se os estados ruins e prejudiciais que ainda não surgiram surgirem
em mim, isso poderá resultar em meu prejuízo’ ... nem ao pensamento:<span
style="mso-spacerun: yes">  </span>‘Se os estados benéficos que já surgiram em
mim cessarem, isso poderá resultar em meu prejuízo.’ Assim, ele não não teme a transgressão.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“É dessa
forma, amigo, que aquele que não é ardente e que não teme a transgressão é
incapaz de se iluminar, incapaz de alcançar Nibbana, incapaz de alcançar a
insuperável segurança contra o cativeiro.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como,
amigo, alguém é ardente? Aqui, amigo, um bhikkhu desperta o ardor pensando: ‘Se
os estados ruins e prejudiciais que ainda não surgiram surgirem em mim, isso
poderá resultar em meu prejuízo’ ... e pensando: ‘Se os estados benéficos que
já surgiram em mim cessarem, isso poderá resultar em meu prejuízo.’ Assim, ele
é ardente.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como,
amigo, ele teme a transgressão? Aqui, amigo, um bhikkhu teme o pensamento: ‘Se
os estados ruins e prejudiciais que ainda não surgiram surgirem em mim, isso
poderá resultar em meu prejuízo’ ... e ao pensamento:<span style="mso-spacerun:
yes">  </span>‘Se os estados benéficos que já surgiram em mim cessarem, isso poderá
resultar em meu prejuízo.’ Assim, ele teme a transgressão.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“É dessa
forma, amigo, que aquele que é ardente e que teme a transgressão é capaz de se
iluminar, capaz de alcançar Nibbana, capaz de alcançar a insuperável segurança
contra o cativeiro.”<o:p></o:p></span></p>




