<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião o Abençoado estava em Savatthi, no palácio da mãe de
Migara, no Parque do Oriente.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. Então,
Sakka, o senhor dos devas, foi até o Abençoado e depois de cumprimentá-lo ficou
em pé a um lado e disse: “Venerável senhor, como em resumo um bhikkhu é
libertado através da destruição do desejo, aquele que alcançou o objetivo
último, a suprema segurança contra o cativeiro, a vida santa consumada, o
objetivo supremo, aquele que é o líder entre devas e humanos?” [<a href="#N1">1</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>3. “Aqui, senhor dos devas, um bhikkhu ouviu que não vale a pena se
apegar a nada. Quando um bhikkhu ouve que não vale a pena se apegar a nada, ele
compreende tudo de modo direto; compreendendo tudo de modo direto, ele
compreende tudo completamente; tendo compreendido tudo completamente, qualquer
coisa que ele sinta, quer seja prazerosa, dolorosa ou nem dolorosa, nem
prazerosa, ele permanece contemplando a impermanência nessas sensações, contemplando
o desaparecimento, contemplando a cessação, contemplando a renúncia.
Contemplando desse modo, ele não se apega a nada no mundo. Não se apegando, ele
não fica agitado. Sem estar agitado, ele realiza Nibbana. [<a href="#N2">2</a>]
<a name=R2></a>Ele compreende que: ‘O nascimento foi destruído, a vida santa
foi vivida, o que deveria ser feito foi feito, não há mais vir a ser a nenhum
estado.’ Em resumo, dessa forma, senhor dos devas, aquele bhikkhu é libertado
através da destruição do desejo, aquele que alcançou o objetivo último, a
suprema segurança contra o cativeiro, a vida santa consumada, o objetivo
supremo, aquele que é o líder entre devas e humanos.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. Então,
Sakka, o senhor dos devas, satisfeito e contente com as palavras do Abençoado,
homenageou o Abençoado e mantendo-o à sua direita, desapareceu de uma vez. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. Agora,
naquela ocasião o venerável Maha Moggallana estava sentado não muito distante
do Abençoado. Ele então pensou: “Aquele deva, ao demonstrar satisfação,
penetrou o significado das palavras do Abençoado, ou não? Suponhamos que eu
descobrisse se ele compreendeu ou não.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. Então,
com a mesma rapidez com que um homem forte pode estender o seu braço flexionado
ou flexionar o seu braço estendido, o venerável Maha Moggallana desapareceu do palácio
da mãe de Migara no Parque do Oriente e apareceu entre os devas do Trinta e
Três.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. Agora,
naquela ocasião Sakka, o senhor dos devas, desfrutava dos prazeres dos cinco
tipos de música divina, e ele assim desfrutava no Parque das Delícias da Única
Flor de Lótus. Ao ver o venerável Maha Moggallana vindo à distância, ele
dispensou a música e foi até o venerável Maha Moggallana e disse: “Venha
estimado senhor Moggallana! Bem vindo estimado senhor Moggallana! Já faz muito
tempo desde a última vez que o estimado senhor Moggallana encontrou uma
oportunidade para vir aqui. Que o estimado senhor Moggallana sente; este
assento está preparado”. O venerável Maha Moggallana sentou no assento que
havia sido preparado e Sakka tomou um assento mais baixo e sentou a um lado. O
venerável Maha Moggallana então perguntou:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8.
“Kosiya,[<a href="#N3">3</a>] <a name=R3></a>como o Abençoado explicou de forma
resumida a libertação através da destruição do desejo? Seria bom se também
pudéssemos ouvir esse relato.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Estimado
senhor Moggallana, estamos tão ocupados, temos muito que fazer, não somente com
os nossos próprios afazeres, mas também com os afazeres dos devas do Trinta e
Três. Além disso, estimado senhor Moggallana, aquilo que é bem ouvido, bem
aprendido, bem cuidado, bem lembrado, não desaparece de repente. Estimado
senhor Moggallana, certa vez ocorreu que irrompeu uma guerra entre os devas e
os asuras. [<a href="#N4">4</a>] <a name=R4></a>Nessa guerra, os devas venceram
e os asuras foram derrotados. Quando venci essa guerra e retornei como
conquistador, fiz com que se construísse o Palácio Vejayanta. Estimado senhor
Moggallana, o Palácio Vejayanta possui cem torres, e cada torre possui
setecentos cômodos, e em cada cômodo há sete ninfas, e com cada ninfa há sete
donzelas. Você gostaria de ver o encantamento que é o Palácio Vejayanta, bom
senhor Moggallana ?” O venerável Maha Moggallana concordou em silêncio.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. Então,
Sakka, o senhor dos devas e o rei divino Vessavana [<a href="#N5">5</a>] <a
name=R5></a>foram até o Palácio Vejayanta, dando precedência ao venerável Maha
Moggallana. Quando as donzelas de Sakka viram o venerável Maha Moggallana vindo
à distância, elas ficaram desconcertadas e envergonhadas e foram cada uma para
os seus quartos. Tal como uma nora fica desconcertada e envergonhada ao ver o
sogro, assim também, quando as donzelas de Sakka viram o venerável Maha
Moggallana vindo à distância elas ficaram desconcertadas e envergonhadas e
foram cada uma para os seus quartos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. Então, Sakka,
o senhor dos devas e o rei divino Vessavana fizeram com que o venerável Maha
Moggallana andasse por todas partes e explorasse o Palácio Vejayanta: “Veja,
estimado senhor Moggallana, o encantamento deste Palácio Vejayanta! Veja,
estimado senhor Moggallana, o encantamento deste Palácio Vejayanta!”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Isto faz
justiça ao venerável Kosiya, como alguém que no passado realizou méritos; e
sempre que os seres humanos vêm algo encantador, eles dizem: ‘Senhores, isso
faz justiça aos devas do Trinta e Três!’ Isso faz justiça ao venerável Kosiya,
como alguém que no passado realizou méritos.”<o:p></o:p></span></p>



<p class=Normal><a name=T11></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>11. Então, o venerável Maha Moggallana pensou o seguinte: “Esse deva
está vivendo de forma muito negligente. E se eu criasse nele um senso de
urgência?” Então, o venerável Maha Moggallana realizou uma grande façanha com
os seus poderes supra-humanos, com a ponta do dedo ele fez com que o Palácio
Vejayanta balançasse e tremesse. [<a href="#N6">6</a>] <a name=R6></a>Sakka e o
rei divino Vessavana e os devas do Trinta e Três ficaram assombrados e
surpresos e disseram: “Senhores, é maravilhoso, é admirável, que poder e força
o contemplativo possui, que apenas com a ponta do dedo ele faz com que o
paraíso balance e trema!” <o:p></o:p></span></p>



<p class=Normal><a name=T12></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>12. Quando o venerável Maha Moggallana percebeu que Sakka, o senhor dos
devas, estava estimulado com um senso de urgência com os cabelos em pé, ele
perguntou: “Kosiya, como o Abençoado explicou de forma sumária a libertação
através da destruição do desejo? Seria bom se também pudéssemos ouvir esse
relato.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Estimado
venerável Moggallana, eu fui até o Abençoado e depois de cumprimentá-lo fiquei em
pé a um lado e disse: ‘Venerável senhor, ... [igual ao verso 2] ... entre devas
e humanos?’ Quando isso foi dito, estimado venerável Moggallana, o Abençoado me
disse: “Aqui. senhor dos devas,<span style='mso-spacerun:yes'>  </span>...
[igual ao verso 3] ... entre devas e humanos.’ Assim foi como o Abençoado me
explicou de modo sumário a libertação através da destruição do desejo, estimado
venerável Moggallana.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. Assim,
o venerável Maha Moggallana ficou satisfeito e contente com as palavras de
Sakka, o senhor dos devas. Então, com a mesma rapidez com que um homem forte
pode estender o seu braço flexionado ou flexionar o seu braço estendido, ele
desapareceu dos devas do Trinta e Três e apareceu no palácio da mãe de Migara
no Parque do Oriente.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14. Então,
logo depois que o venerável Maha Moggallana partiu, os acompanhantes de Sakka,
o senhor dos devas, perguntaram: “Estimado senhor, esse foi o seu mestre, o
Abençoado?” – “Não, estimados senhores, ele não é o meu mestre, o Abençoado.
Ele é um dos meus companheiros na vida santa, o venerável Maha Moggallana.” [<a
href="#N7">7</a>] <a name=R7></a>– “Muito bem senhor, é um ganho para você que
o seu companheiro na vida santa tenha tanto poder e força. Oh, quanto mais
então não terá o Abençoado que é o seu mestre!”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. Então o
venerável Maha Moggallana foi até o Abençoado e depois de cumprimentá-lo sentou
a um lado e perguntou: “Venerável senhor, o Abençoado se recorda haver explicado
de forma sumária – para um dos devas renomados que possui um grande séquito – a
libertação através da destruição do desejo?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu me
lembro ter feito isso, Moggallana. Aqui Sakka, o senhor do devas, veio até mim
e depois de me cumprimentar, ficou em pé <span
style='mso-spacerun:yes'> </span>a um lado e perguntou: ‘Venerável senhor, como
em resumo um bhikkhu é libertado através da destruição do desejo, aquele que
alcançou o objetivo último, a suprema segurança contra o cativeiro, a vida
santa consumada, o objetivo supremo, aquele que é o líder entre devas e
humanos?’ Quando isso foi dito, eu respondi: ‘Aqui, senhor dos devas, um
bhikkhu ouviu que não vale a pena se apegar a nada. Quando um bhikkhu ouviu que
não vale a pena se apegar a nada, ele compreende tudo de modo direto;
compreendendo tudo de modo direto, ele compreende tudo completamente; tendo
compreendido tudo completamente, qualquer coisa que ele sinta, quer seja
prazerosa, dolorosa ou nem dolorosa, nem prazerosa, ele permanece contemplando
a impermanência nessas sensações, contemplando o desaparecimento, contemplando
a cessação, contemplando a renúncia. Contemplando desse modo, ele não se apega
a nada no mundo. Não se apegando, ele não fica agitado. Sem estar agitado, ele
realiza Nibbana. Ele compreende que: “O nascimento foi destruído, a vida santa
foi vivida, o que deveria ser feito foi feito, não há mais vir a ser a nenhum
estado.” Em resumo, dessa forma, senhor dos devas, aquele bhikkhu é libertado
através da destruição do desejo, aquele que alcançou o fim último, a suprema
segurança contra o cativeiro, a vida santa consumada, o objetivo último, aquele
que é o líder entre devas e humanos.’ Assim é como me lembro haver explicado de
forma resumida para Sakka, o senhor dos devas, a libertação através da destruição
do desejo.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o
que disse o Abençoado. O venerável Maha Moggallana ficou satisfeito e contente
com as palavras do Abençoado.<span style='mso-spacerun:yes'>  </span><o:p></o:p></span></p>

