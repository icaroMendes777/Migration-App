<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[2] MA explica este trecho da seguinte forma:
“Tudo” (<i>sabbe dhamma</i>) são os cinco agregados, as doze bases, os dezoito
elementos. A eles “não vale a pena se apegar ”, através do desejo e idéias,
porque eles se revelam na verdade diferentes do que imaginamos ao agarrá-los:
nós nos apegamos a eles como se fossem permanentes, prazerosos e como ‘eu’, e
eles se revelam impermanentes, insatisfatórios e não-eu. Ele “compreende</span><span
lang=PT-BR style='mso-ansi-language:PT-BR'> tudo de modo direto” que eles são
impermanentes, insatisfatórios e não-eu, e “compreende tudo completamente” ao
analisá-los do mesmo modo. “Contemplando a impermanência,” etc., é realizado
através do insight do surgimento e origem e da destruição e cessação. “Ele não
se apega” a nenhuma formação através do desejo e das idéias, ele não se agita
devido ao desejo, e realiza Nibbana através da destruição de todas as impurezas<span
style='mso-bidi-font-weight:bold'>. [<a href="#R2">Retorna</a>]<o:p></o:p></span></span></p>


