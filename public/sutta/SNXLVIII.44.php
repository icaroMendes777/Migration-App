<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim ouvi,
Em certa ocasião, o Abençoado estava em Savatthi, no Portão do Oriente. Lá ele
se dirigiu ao Ven.
Sariputta desta forma: <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sariputta,
você tem convicção de que a faculdade da convicção, quando desenvolvida e
cultivada, possui o Imortal como seu fundamento, possui o Imortal como seu
destino, possui o Imortal como seu objetivo? Você tem convicção que a faculdade
da energia … atenção plena … concentração ... sabedoria, quando desenvolvida e
cultivada, possui o Imortal como seu fundamento, possui o Imortal como seu
destino, possui o Imortal como seu objetivo?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável senhor, eu não
me baseio na convicção no Abençoado ao considerar isto: que a faculdade da
convicção… energia … atenção plena … concentração ... sabedoria, quando
desenvolvida e cultivada, possui o Imortal como seu fundamento, possui o
Imortal como seu destino, possui o Imortal como seu objetivo. Aqueles que não compreenderam,
viram, penetraram, realizaram ou contataram por meio da sabedoria, teriam que
tomar por base a convicção nos outros ao considerar isto: que a faculdade da
convicção ... energia … atenção plena … concentração ... sabedoria, quando
desenvolvida e cultivada, possui o Imortal como seu fundamento, possui o
Imortal como seu destino, possui o Imortal como seu objetivo. Mas aqueles que
compreenderam, viram, penetraram, realizaram ou contataram por meio da
sabedoria eles não teriam dúvida ou perplexidade em relação a isto: que a
faculdade da convicção… energia … atenção plena … concentração ... sabedoria,
quando desenvolvida e cultivada, possui o Imortal como seu campo, possui o
Imortal como seu destino, possui o Imortal como seu objetivo. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>E quanto a mim, venerável
senhor, eu compreendi, vi, penetrei, realizei e contatei por meio da sabedoria.
Eu não tenho dúvida ou perplexidade em relação a isto: que a faculdade da
convicção … energia … atenção plena … concentração ... sabedoria, quando
desenvolvida e cultivada, possui o Imortal como seu fundamento, possui o
Imortal como seu destino, possui o Imortal como seu objetivo.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Muito bem, Sariputta!
Aqueles que não compreenderam<span style="mso-spacerun: yes">  </span>... têm
que tomar por base a convicção nos outros em relação a isto ... Mas aqueles que
compreenderam ... eles não têm dúvida ou perplexidade em relação a isto: que a
faculdade da convicção… energia … atenção plena … concentração ... sabedoria,
quando desenvolvida e cultivada, possui o Imortal como seu fundamento, possui o
Imortal como seu destino, possui o Imortal como seu objetivo.” </span></p>


