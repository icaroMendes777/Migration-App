<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
há árvores imensas com sementes minúsculas e troncos enormes que circundam
outras árvores, e as árvores que elas circundam ficam curvadas, contorcidas e divididas.
E quais são essas árvores imensas com sementes minúsculas e troncos enormes? A
assattha, a figueira-de –bengala, a pilakkha, a udumbara, a kacchaka e a
kapitthana: essas são aquelas árvores imensas com sementes minúsculas e troncos
enormes que circundam outras árvores, e as árvores que elas circundam ficam
curvadas, contorcidas e divididas. Da mesma maneira, bhikkhus, quando algum
membro de um clã aqui abandonou os prazeres sensuais e deixou a vida em família
para seguir a vida santa, ele fica curvado, contorcido e dividido por causa
daqueles mesmos prazeres sensuais ou por causa de outras coisas ainda piores. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Essas
cinco, bhikkhus, são obstruções, obstáculos que circundam a mente,
enfraquecedoras da sabedoria. Quais cinco? O desejo sensual é uma obstrução, um
obstáculo que circunda a mente, enfraquecedor da sabedoria ... A preguiça e o
torpor ... A inquietação e a ansiedade ... A dúvida é uma obstrução ...
enfraquece a sabedoria. Essas são as cinco obstruções, obstáculos que circundam
a mente, enfraquecedoras da sabedoria.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Esses sete
fatores da iluminação, bhikkhus, são não-obstruções, não-obstáculos que não
circundam a mente; quando desenvolvidos e cultivados eles conduzem à realização
do fruto do verdadeiro conhecimento e libertação. Quais sete? O fator da
iluminação da atenção plena é uma não-obstrução ... O fator da iluminação da
equanimidade é uma não-obstrução. Esses sete fatores da iluminação são
não-obstruções, <a name=PVW>não-obstáculos</a> que não circundam a mente; quando
desenvolvidos e cultivados eles conduzem à realização do fruto do verdadeiro
conhecimento e libertação.”<o:p></o:p></span></p>


