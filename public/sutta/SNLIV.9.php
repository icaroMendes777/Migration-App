<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim ouvi.
Em certa ocasião, o Abençoado estava em Vesali na Grande Floresta no Salão com
um pico na cumeeira. Agora, naquela ocasião o Abençoado estava discursando para
os bhikkhus sobre vários tipos de coisas repulsivas, estava falando em louvor das coisas repulsivas, estava falando em louvor do desenvolvimento da meditação das coisas repulsivas. [<a href="#N1">1</a>] <a name=R1></a>Então o Abençoado se
dirigiu aos bhikkhus da seguinte forma:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
eu quero estar em retiro por meio mês. Eu não devo ser abordado por ninguém
exceto aquele que me traz a comida esmolada.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim, venerável
senhor,” aqueles bhikkhus responderam e ninguém se aproximou do Abençoado
exceto aquele que lhe levou a comida esmolada. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então,
aqueles bhikkhus pensando: “O Abençoado discursou sobre vários tipos de
coisas repulsivas, falou em louvor das coisas repulsivas, falou em louvor do
desenvolvimento da meditação das coisas repulsivas,” permaneceram dedicados ao
desenvolvimento da meditação das coisas repulsivas nos seus muitos aspectos e
fatores. Sentido repulsa, vergonha e nojo deste corpo, eles foram em busca de
um assassino. Num dia, dez bhikkhus usaram a faca ou num dia, vinte ou trinta
bhikkhus usaram a faca. [<a href="#N2">2</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Então, quando havia passado meio mês, o Abençoado emergiu do seu
isolamento e se dirigiu ao Venerável Ananda: “Porque, Ananda, a Sangha dos
bhikkhus parece estar tão diminuída?<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, é porque o Abençoado discursou sobre vários tipos de coisas repulsivas,
falou em louvor das coisas repulsivas, falou em louvor do desenvolvimento da
meditação das coisas repulsivas e aqueles bhikkhus pensando: ‘O Abençoado discursou
sobre vários tipos de coisas repulsivas, falou em louvor das coisas repulsivas, falou
em louvor do desenvolvimento da meditação das coisas repulsivas,’ permaneceram
dedicados ao desenvolvimento da meditação das coisas repulsivas nos seus muitos
aspectos e fatores. Sentindo repulsa, vergonha e nojo deste corpo, eles foram
em busca de um assassino. Num dia, dez bhikkhus usaram a faca ou num dia, vinte
ou trinta bhikkhus usaram a faca. Seria bom, venerável senhor, se o Abençoado
explicasse um outro método de modo que esta sangha dos bhikkhus possa se
estabelecer no conhecimento supremo.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bem então,
Ananda, reúna no salão de assembléias todos os bhikkhus que estão vivendo em
Vesali.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
venerável senhor,” o Venerável Ananda respondeu e ele reuniu no salão de
assembléias todos os bhikkhus que estavam vivendo em Vesali, tantos quantos
havia. Então ele foi até o Abençoado e lhe disse: “ A sangha dos bhikkhus está
reunida, venerável senhor. Que o Abençoado venha quando lhe for conveniente.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, o
Abençoado foi até o salão de assembléias, sentou num assento que havia sido
preparado e se dirigiu aos bhikkhus da seguinte forma:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
esta concentração através da atenção plena na respiração, quando desenvolvida e
cultivada, é pacífica e sublime, uma permanência ambrosíaca prazerosa, e esta
dispersa e extingue no ato os estados ruins e prejudiciais sempre que estes
surjam. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Tal como,
bhikkhus, no último mês do verão, quando uma massa de poeira se forma num
redemoinho, uma grande nuvem de chuva fora de época a dispersa e extingue no
ato, da mesma forma a concentração através da atenção plena na respiração,
quando desenvolvida e cultivada, é pacífica e sublime, uma permanência
ambrosíaca, prazerosa, e ela dispersa e extingue no ato os estados ruins e
prejudiciais sempre que estes surgirem. E como isso é feito?<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
bhikkhus, um bhikkhu, dirigindo-se à floresta ou à sombra de uma árvore ou a um
local isolado; senta-se com as pernas cruzadas, mantém o corpo ereto e
estabelecendo a plena atenção à sua frente, ele inspira com atenção plena justa, ele expira com atenção plena justa ... Ele treina dessa forma: ‘Eu
inspiro contemplando a renúncia’; ele treina dessa forma: ‘Eu expiro
contemplando a renúncia.’[<a href="#N3">3</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“É assim, bhikkhus, que a concentração através da atenção plena na
respiração é desenvolvida e cultivada, de modo que seja pacífica e sublime, uma
permanência ambrosíaca, prazerosa e ela dispersa e extingue no ato os estados
ruins e prejudiciais sempre que estes surgirem.”<o:p></o:p></span></p>




