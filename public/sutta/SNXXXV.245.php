perguntou-lhe,“Até que ponto, amigo, diz-se que a visão de um bhikkhu é bem
purificada?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando, amigo, um bhikkhu
compreende como na verdade é a origem e a cessação dos quatro grandes elementos
(terra, água, fogo e ar), desse modo se diz que a sua visão é bem
purificada.”<span style="mso-spacerun: yes">  </span><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O primeiro bhikkhu,
insatisfeito com a resposta desse bhikkhu, foi até um outro bhikkhu e ao chegar
perguntou-lhe,“Até que ponto, amigo, diz-se que a visão de um bhikkhu é bem
purificada?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando, amigo, um bhikkhu
compreende como na verdade é que tudo </span>que está sujeito ao surgimento
está sujeito à cessação<span lang=PT-BR style='mso-ansi-language:PT-BR'>, desse
modo se diz que a sua visão é bem purificada.”<span style="mso-spacerun: yes"> 
</span><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O primeiro bhikkhu,
insatisfeito com a resposta desse bhikkhu, foi, então, até o Abençoado e depois
de cumprimentá-lo sentou a um lado e relatou as conversas com os outros
bhikkhus. O Abençoado disse: <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“B<a name=tree>hikk</a>hu,
