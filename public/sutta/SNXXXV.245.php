árvore das charadas. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então o primeiro homem,
insatisfeito com a resposta desse homem, iria até um outro homem que tivesse
visto uma árvore das charadas e ao chegar diria, ‘Como é, bom homem, uma árvore
das charadas?’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O outro diria, ‘Uma árvore
das charadas possui densa folhagem, meu bom homem e proporciona uma sombra
compacta, como uma figueira de bengala.’ Pois quando ele a viu, assim é como se
encontrava a árvore das charadas. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Do mesmo modo, bhikkhu,
aqueles nobres responderam de acordo com a inclinação deles,
exatamente como a visão deles se tornou bem purificada. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“<a name=fort>Suponha</a>,
bhikkhu, que um rei tivesse uma cidade fronteiriça com sólidas muralhas,
proteções e abóbadas, e com seis portões. O guardião dos portões seria sábio,
competente e inteligente; alguém que não permite a entrada de desconhecidos e
admite que entrem os conhecidos. Um par d<a name=swift>e men</a>sageiros ágeis,
vindos do leste, diriam ao guardião, ‘Onde, bom homem, está o comandante desta
fortaleza?’ Ele diria, ‘Ali está ele, senhores, sentado na praça central.’ O
ágil par de mensageiros transmitiriam a sua mensagem precisa<span
style="mso-spacerun: yes">  </span>ao comandante da fortaleza e então
retornariam<span style="mso-spacerun: yes">  </span>pelo mesmo caminho pelo
qual eles tinham vindo. Então um ágil par de mensageiros vindos do oeste ... do
norte ... do sul, diriam ao guardião, ‘Onde, bom homem, está o comandante desta
fortaleza?’ Ele diria, ‘Ali está ele, senhores, sentado na praça central.’ O
ágil par de mensageiros transmitiriam a sua mensagem precisa ao comandante da
fortaleza e então retornariam pelo mesmo caminho pelo qual tinham vindo. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu citei este símile,
bhikkhu, para transmitir uma idéia. A idéia é a seguinte: a fortaleza
representa este corpo – consistindo dos quatro grandes elementos - procriado por
uma mãe e um pai, construído com arroz cozido e mingau, sujeito à impermanência, a
ser gasto e pulverizado, à dissolução e desintegração. Os seis portões
representam as seis bases sensuais internas. O guardião representa a atenção
plena. O ágil par de mensageiros representam a tranqüilidade, <i>(samatha),</i>
e o insight, <i>(vipassana).</i> O comandante da fortaleza representa a
consciência. A praça central representa os quatro grandes elementos: o elemento
terra, o elemento água, o elemento fogo e o elemento ar. A mensagem precisa
representa a Libertação, <i>(nibbana).</i> O caminho pelo qual eles vieram
representa o nobre caminho óctuplo: entendimento correto, pensamento correto,
linguagem correta, ação correta, modo de vida correto, esforço correto, atenção
plena correta, concentração correta.” <o:p></o:p></span></p>


