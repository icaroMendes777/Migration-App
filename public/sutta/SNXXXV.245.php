a viu, assim é como se encontrava a árvore das charadas.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então o primeiro homem,
insatisfeito com a resposta desse homem, iria até um outro homem que tivesse
visto uma árvore das charadas e ao chegar diria, ‘Como é, bom homem, uma árvore
das charadas?’<span style="mso-spacerun: yes">  </span><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O outro diria, ‘Uma árvore
das charadas é vermelha, meu bom homem, como um pedaço de carne.’ Pois quando
ele a viu, assim é como se encontrava a árvore das charadas. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então o primeiro homem,
insatisfeito com a resposta desse homem, iria até um outro homem que tivesse
