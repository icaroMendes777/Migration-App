pelo apego, desse modo se diz que a sua visão é bem purificada.”<span
style="mso-spacerun: yes">  </span><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O primeiro bhikkhu,
