<p class=Normal style='margin-left:.75in'>



<p class=Normal> Ouvi que em certa ocasião o Ven. Sariputta estava em Savatthi, no
Bosque de Jeta, no Parque de Anathapindika. Agora, naquela ocasião uma idéia
perniciosa havia surgido na mente do Ven. Yamaka: <o:p></o:p></span></p>


<p class=Normal> &quot;Da forma como eu entendo o Dhamma ensinado pelo Abençoado, um
bhikkhu que não possui mais impurezas, na dissolução do corpo, é aniquilado,
extinto e não existe após a morte.&quot; Um grande número de bhikkhus ouviu,
&quot;Eles dizem que esta idéia perniciosa surgiu na mente na mente do Ven.
Yamaka: 'Da forma como eu entendo o Dhamma ensinado pelo Abençoado, um bhikkhu
que não possui mais impurezas, na dissolução do corpo, é aniquilado, extinto e
não existe após a morte.'&quot; <o:p></o:p></span></p>


<p class=Normal> Assim eles se dirigiram até o Ven. Yamaka e chegando eles se
cumprimentaram. Após a troca de saudações amigáveis e corteses, eles sentaram a
um lado e disseram: &quot;É verdade, amigo Yamaka, que esta idéia perniciosa
surgiu na sua mente: 'Da forma como eu entendo o Dhamma ensinado pelo
Abençoado, um bhikkhu que não possui mais impurezas, na dissolução do corpo, é
aniquilado, extinto e não existe após a morte.'<o:p></o:p></span></p>


<p class=Normal> &quot;Sim, amigos. Da forma como entendo o Dhamma ensinado pelo
Abençoado, um bhikkhu que não possui mais impurezas, na dissolução do corpo, é
aniquilado, extinto e não existe após a morte.'<o:p></o:p></span></p>


<p class=Normal> &quot;Não diga isso, amigo Yamaka. Não deturpe o Abençoado; não é bom
deturpar o Abençoado. O Abençoado não falaria dessa forma: 'Um bhikkhu que não
possui mais impurezas, na dissolução do corpo, é aniquilado, extinto e não
existe após a morte.'&quot;<o:p></o:p></span></p>

<p class=Normal> Apesar disso, mesmo tendo sido pressionado, questionado e examinado por
eles desta forma, o Ven. Yamaka, ainda assim, obstinadamente<span
style="mso-spacerun: yes">  </span>manteve a sua idéia perniciosa e continuou
insistindo nela: 'Da forma como entendo o Dhamma ensinado pelo Abençoado, um
bhikkhu que não possui mais impurezas, na dissolução do corpo, é aniquilado,
extinto e não existe após a morte.'<o:p></o:p></span></p>


<p class=Normal> Visto que os bhikkhus foram incapazes de fazer com que ele se separasse
dessa idéia perniciosa, eles se dirigiram até o Ven. Sariputta e depois de
cumprimentá-lo sentaram a um lado e disseram: &quot;Amigo Sariputta, esta idéia
perniciosa surgiu na mente do Ven. Yamaka: 'Da forma como entendo o Dhamma ensinado
pelo Abençoado, um bhikkhu que não possui mais impurezas, na dissolução do
corpo, é aniquilado, extinto e não existe após a morte.' Seria bom se o amigo
fosse até o Ven. Yamaka por compaixão pelo seu bem-estar.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR>O Ven. Sariputta concordou permanecendo em silêncio.</span></p>

<p class=Normal><span lang=PT-BR>Então, naquela noite o Ven. Sariputta deixou o seu
isolamento e foi até o Ven. Yamaka e ao chegar ambos se cumprimentaram. Quando
a conversa amigável e cortês havia terminado, ele sentou a um lado e disse:
&quot;É verdade, meu amigo Yamaka, que essa idéia perniciosa surgiu na sua
mente: 'Da forma como entendo o Dhamma ensinado pelo Abençoado, um bhikkhu que
não possui mais impurezas, na dissolução do corpo, é aniquilado, extinto e não
existe após a morte.' </span></p>

<p class=Normal><span lang=PT-BR>&quot;Sim, meu amigo Sariputta. Da forma como entendo o
Dhamma ensinado pelo Abençoado, um bhikkhu que não possui mais impurezas, na
dissolução do corpo, é aniquilado, extinto e não existe após a morte.&quot; </span></p>

<p class=Normal><span lang=PT-BR>“O que você pensa, amigo Yamaka, a forma é permanente ou
impermanente?</span></p>

<p class=Normal><span lang=PT-BR>“Impermanente, amigo. </span></p>

<p class=Normal><span lang=PT-BR>&quot;E aquilo que é impermanente é sofrimento ou
felicidade? </span></p>

<p class=Normal><span lang=PT-BR>“Sofrimento, amigo.</span></p>

<p class=Normal><span lang=PT-BR>“E é adequado considerar o que é impermanente, sofrimento,
sujeito a mudanças como: ‘Isso é meu. Isso sou eu. Isso é o meu eu’? </span></p>

<p class=Normal><span lang=PT-BR>“Não, amigo.</span></p>

<p class=Normal><span lang=PT-BR>“... a sensação é permanente ou impermanente?</span></p>

<p class=Normal><span lang=PT-BR>“Impermanente, amigo.</span></p>

<p class=Normal><span lang=PT-BR>“... a percepção é permanente ou impermanente?</span></p>

<p class=Normal><span lang=PT-BR>“Impermanente, amigo.</span></p>

<p class=Normal><span lang=PT-BR>“... as formações volitivas são permanentes ou impermanentes?</span></p>

<p class=Normal><span lang=PT-BR>“Impermanentes, amigo. </span></p>

<p class=Normal><span lang=PT-BR>“O que você pensa, amigo Yamaka, a consciência é permanente
ou impermanente? </span></p>

<p class=Normal><span lang=PT-BR>“Impermanente, amigo.</span></p>

<p class=Normal><span lang=PT-BR>“E aquilo que é impermanente é sofrimento ou felicidade?</span></p>

<p class=Normal><span lang=PT-BR>“Sofrimento, amigo.</span></p>

<p class=Normal><span lang=PT-BR>“E é adequado considerar o que é impermanente, sofrimento,
sujeito a mudanças como: 'Isso é meu. Isso sou eu. Isso é o meu eu’?<span
style="mso-spacerun: yes">  </span></span></p>

<p class=Normal><span lang=PT-BR>“Não, amigo. </span></p>

<p class=Normal><span lang=PT-BR>“O que você pensa, você considera a forma como sendo o
Tathagata?</span></p>

<p class=Normal><span lang=PT-BR>“Não, meu amigo.</span></p>

<p class=Normal><span lang=PT-BR>“Você considera a sensação como sendo o Tathagata?</span></p>

<p class=Normal><span lang=PT-BR>“Não, meu amigo.</span></p>

<p class=Normal><span lang=PT-BR>“Você considera a percepção como sendo o Tathagata?</span></p>

<p class=Normal><span lang=PT-BR>“Não, meu amigo.</span></p>

<p class=Normal><span lang=PT-BR>“Você considera as formações volitivas como sendo o Tathagata?</span></p>

<p class=Normal><span lang=PT-BR>“Não, meu amigo.</span></p>

<p class=Normal><span lang=PT-BR>“Você considera a consciência como sendo o Tathagata?</span></p>

<p class=Normal><span lang=PT-BR>“Não, meu amigo.</span></p>

<p class=Normal><span lang=PT-BR>“O que você pensa, você considera que o Tathagata está na forma?
... separado da forma? ... na sensação? ... separado da sensação? ... na
percepção? ... separado da percepção? ... nas formações volitivas? ... separado das
formações volitivas? ... na consciência? ... separado da consciência?&quot; </span></p>

<p class=Normal><span lang=PT-BR>“Não, meu amigo.</span></p>

<p class=Normal><span lang=PT-BR>“O que você pensa, você considera que o Tathagata é o
conjunto da forma-sensação-percepção-formações volitivas-consciência? </span></p>

<p class=Normal><span lang=PT-BR>“Não, meu amigo.</span></p>

<p class=Normal><span lang=PT-BR>“Você considera que o Tathagata não tem forma, não tem
sensação, não tem percepção, não tem formações volitivas, não tem consciência? </span></p>

<p class=Normal><span lang=PT-BR>“Não, meu amigo.</span></p>

<p class=Normal><span lang=PT-BR>“Mas, amigo, quando você não consegue determinar o Tathagata
como real e presente nesta mesma vida, é apropriado que você declare,<span
style="mso-spacerun: yes">  </span>'Da forma como entendo o Dhamma ensinado
pelo Abençoado, um bhikkhu que não possui mais impurezas, na dissolução do
corpo, é aniquilado, extinto e não existe após a morte'? </span></p>

<p class=Normal><span lang=PT-BR>&quot;Anteriormente, amigo Sariputta, eu acreditava
tolamente nessa idéia perniciosa. Porém agora, tendo ouvido a sua explicação do
Dhamma, eu abandonei aquela idéia perniciosa e compreendi o Dhamma.</span></p>

<p class=Normal><span lang=PT-BR>&quot;Então amigo Yamaka, como você responderia se fosse
perguntado desta forma: Um bhikkhu, um nobre, sem impurezas mentais: o que
acontecerá com ele na dissolução do corpo após a morte? </span></p>

<p class=Normal><span lang=PT-BR>&quot;Perguntado dessa forma, eu responderia, 'A forma é
impermanente, as sensações ..., as percepções ..., as formações volitivas ..., a consciência é
impermanente. Aquilo que é impermanente é sofrimento. Aquilo que é sofrimento
cessou e chegou ao seu fim.&quot; </span></p>

<p class=Normal><span lang=PT-BR>&quot;Muito bem, meu amigo Yamaka. Muito bem. Nesse caso eu
lhe contarei um símile para ampliar ainda mais o seu entendimento desse ponto.
Suponha que houvesse um chefe de família ou o filho de um chefe de família,
rico, próspero, com muitas posses, protegido por um guarda-costas. Então
suponha que um homem aparecesse, desejando arruiná-lo, desejando causar-lhe
dano, colocá-lo em perigo, matá-lo. O pensamento ocorreria a esse homem: 'Esse
chefe de família, ou o filho de um chefe de família, rico, próspero, com muitas
posses, protegido por um guarda-costas, não será fácil matá-lo pela força. E se
eu me aproximasse dele e o matasse?&quot; </span></p>

<p class=Normal><span lang=PT-BR>&quot;Assim ele iria até o chefe de família, ou o seu filho,
e diria, 'Por favor me aceite como um empregado, senhor.' Com isso o chefe de
família, ou seu filho, tomariam o homem como um empregado. </span></p>

<p class=Normal><span lang=PT-BR>&quot;Tendo sido aceito como um empregado, o homem levantaria
pela manhã antes do seu patrão, iria para a cama à noite somente depois do seu
patrão, fazendo tudo aquilo que o seu patrão ordenasse, sempre agindo para
agradá-lo, dirigindo-se a ele de forma cortês. Então o chefe de família, ou seu
filho, passariam a considerá-lo como um amigo e companheiro e depositariam
confiança nele. Quando o homem se desse conta, 'Esse chefe de família, ou o seu
filho, confiam em mim,' então, encontrando-o num lugar solitário, ele o mataria
com uma faca afiada. </span></p>

<p class=Normal><span lang=PT-BR>&quot;Agora, o que você pensa meu amigo Yamaka? Quando o
homem se dirigiu ao chefe de família, ou seu filho, e lhe disse, 'Por favor me
aceite como um empregado, senhor': ele já não era então um assassino? E embora
ele fosse um assassino, o chefe de família ou seu filho não o reconheceram como
sendo 'meu assassino.' E quando, aceito como um empregado, ele se levantou pela
manhã antes do seu patrão, foi para a cama à noite somente após o seu patrão,
fez tudo aquilo que o seu patrão ordenou, sempre agindo para agradá-lo,
dirigindo-se a ele de forma cortês: ele já não era então um assassino? E embora
ele fosse um assassino, o chefe de família, ou seu filho, não o reconheceram como
sendo 'meu assassino.' E quando ele o encontrou num lugar solitário e o matou
com uma faca afiada: ele já não era então um assassino? E embora ele fosse um
assassino, o chefe de família, ou seu filho, não o reconheceram como sendo 'meu
assassino.'&quot;</span></p>

<p class=Normal><span lang=PT-BR>&quot;Sim, meu amigo.&quot; </span></p>

<p class=Normal><span lang=PT-BR>&quot;Da mesma forma, uma pessoa comum sem instrução que
não respeita os nobres, que não é proficiente nem treinada no Dhamma deles,
que não respeita os homens verdadeiros, que não é proficiente nem treinada
no Dhamma deles supõem que a forma é o eu, ou o eu possuído de forma, ou a
forma como estando no eu, ou o eu como estando na forma. </span></p>

<p class=Normal><span lang=PT-BR>&quot;Ela supõe que a sensação é o eu ... </span></p>

<p class=Normal><span lang=PT-BR>&quot;Ela supõe que a percepção é o eu ... </span></p>

<p class=Normal><span lang=PT-BR>&quot;Ela supõe que as formações volitivas são o eu ... </span></p>

<p class=Normal><span lang=PT-BR>&quot;Ela supõe que a consciência é o eu, ou o eu possuído
de consciência, ou a consciência como estando no eu, ou o eu como estando na
consciência. </span></p>

<p class=Normal><span lang=PT-BR>“Ela não compreende como na verdade é, a forma impermanente
como ‘forma impermanente,’ ... sensação impermanente como ‘sensação
impermanente,’ ... percepção impermanente como ‘percepção impermanente,’ ...
formações volitivas impermanentes como ‘formações volitivas impermanentes,’ ... consciência
impermanente como ‘consciência impermanente.’</span></p>

<p class=Normal><span lang=PT-BR>“Ela não compreende como na verdade é, a forma dolorosa
como ‘forma dolorosa,’ ... sensação dolorosa como ‘sensação dolorosa,’ ...
percepção dolorosa como ‘percepção dolorosa,’ ... formações volitivas dolorosas como
‘formações volitivas dolorosas,’ ... consciência dolorosa como ‘consciência dolorosa.’</span></p>

<p class=Normal><span lang=PT-BR>“Ela não compreende como na verdade é, a forma desprovida
de um eu como ‘forma desprovida de um eu,’ ... sensação desprovida de um eu como
‘sensação desprovida de um eu,’ ... percepção desprovida de um eu como
‘percepção desprovida de um eu,’ ... formações volitivas desprovidas de um eu como
‘formações volitivas desprovidas de um eu,’ ... consciência desprovida de um eu como
‘consciência desprovida de um eu.’</span></p>

<p class=Normal><span lang=PT-BR>“Ela não compreende como na verdade é, a forma condicionada
como ‘forma condicionada,’ ... sensação condicionada como ‘sensação
condicionada,’ ... percepção condicionada como ‘percepção condicionada,’ ...
formações volitivas condicionadas como ‘formações volitivas condicionadas,’ ... consciência
condicionada como ‘consciência condicionada.’</span></p>

<p class=Normal><span lang=PT-BR>“Ela não compreende como na verdade é, a forma assassina
como ‘forma assassina,’ ... sensação assassina como ‘sensação assassina,’ ...
percepção assassina como ‘percepção assassina,’ ... formações volitivas assassinas como
‘formações volitivas assassinas,’ ... consciência assassina como ‘consciência assassina.’</span></p>

<p class=Normal><span lang=PT-BR>&quot;Ela se apega à forma, se agarra à forma e determina
que ela é o seu 'eu.' Ela se apega à sensação ... Ela se apega à percepção ....
Ela se apega às formações volitivas ... Ela se apega à consciência, se agarra à consciência
e determina que ela é o seu 'eu.' Esses cinco agregados influenciados pelo
apego - aos quais ela se apega, se agarra - conduzem ao sofrimento e aflição
por muito tempo. </span></p>

<p class=Normal><span lang=PT-BR>&quot;Mas, amigo, o nobre discípulo bem instruído que
respeita os nobres, que é proficiente e treinado no Dhamma deles, que
respeita os homens verdadeiros, que é proficiente e treinado no Dhamma
deles não supõe que a forma é o eu, ou o eu possuído de forma, ou a forma como
estando no eu, ou o eu como estando na forma. </span></p>

<p class=Normal><span lang=PT-BR>&quot;Ele não supõe que a sensação é o eu ... </span></p>

<p class=Normal><span lang=PT-BR>&quot;Ele não supõe que a percepção é o eu ... </span></p>

<p class=Normal><span lang=PT-BR>&quot;Ele não supõe que as formações volitivas são o eu ... </span></p>

<p class=Normal><span lang=PT-BR>&quot;Ele não supõe que a consciência é o eu, ou o eu
possuído de consciência, ou a consciência como estando no eu, ou o eu como estando
na consciência. </span></p>

<p class=Normal><span lang=PT-BR>“Ele compreende como na verdade é, a forma impermanente
como ‘forma impermanente,’ ... sensação impermanente como ‘sensação
impermanente,’ ... percepção impermanente como ‘percepção impermanente,’ ...
formações volitivas impermanentes como ‘formações volitivas impermanentes,’ ... consciência
impermanente como ‘consciência impermanente.’</span></p>

<p class=Normal><span lang=PT-BR>“Ele compreende como na verdade é, a forma dolorosa como
‘forma dolorosa,’ ... sensação dolorosa como ‘sensação dolorosa,’ ... percepção
dolorosa como ‘percepção dolorosa,’ ... formações volitivas dolorosas como ‘formações volitivas
dolorosas,’ ... consciência dolorosa como ‘consciência dolorosa.’</span></p>

<p class=Normal><span lang=PT-BR>“Ele compreende como na verdade é, a forma desprovida de um
eu como ‘forma desprovida de um eu,’ ... sensação desprovida de um eu como
‘sensação desprovida de um eu,’ ... percepção desprovida de um eu como
‘percepção desprovida de um eu,’ ... formações volitivas desprovidas de um eu como
‘formações volitivas desprovidas de um eu,’ ... consciência desprovida de um eu como
‘consciência desprovida de um eu.’</span></p>

<p class=Normal><span lang=PT-BR>“Ele compreende como na verdade é, a forma condicionada
como ‘forma condicionada,’ ... sensação condicionada como ‘sensação
condicionada,’ ... percepção condicionada como ‘percepção condicionada,’ ...
formações volitivas condicionadas como ‘formações volitivas condicionadas,’ ... consciência
condicionada como ‘consciência condicionada.’</span></p>

<p class=Normal><span lang=PT-BR>“Ele compreende como na verdade é, a forma assassina como
‘forma assassina,’ ... sensação assassina como ‘sensação assassina,’ ...
percepção assassina como ‘percepção assassina,’ ... formações volitivas assassinas como
‘formações volitivas assassinas,’ ... consciência assassina como ‘consciência assassina.’</span></p>

<p class=Normal><span lang=PT-BR>&quot;Ele não se apega à forma, não se agarra à forma e não
determina que ela é o seu 'eu'. Ele não se apega à sensação ... Ele não se
apega à percepção .... Ele não se apega às formações volitivas ... Ele não se apega à
consciência, não se agarra à consciência e não determina que ela é o seu 'eu'.
Esses cinco agregados influenciados pelo apego - aos quais ele não se apega,
não se agarra - conduzem à felicidade e bem-estar por muito tempo. </span></p>

<p class=Normal><span lang=PT-BR>&quot;Assim é, meu amigo Sariputta, para aqueles que têm
pessoas como você como seus companheiros na vida santa, ensinando-os,
advertindo-os por compaixão, desejando o seu bem-estar. Pois agora que ouvi
essa sua explicação do Dhamma, minha mente, através do desapego foi libertada
das impurezas.&quot; </span></p>

<p class=Normal><span lang=PT-BR>Isso foi o que disse o Ven. Sariputta. O Ven. Yamaka ficou
satisfeito e contente com as palavras do Ven. Sariputta.</span></p>


