<p class=Normal> Em certa ocasião, o Abençoado estava entre os Sakyas numa cidade Sakya chamada Devadaha. Lá ele se dirigiu aos bhikkhus:</p>

<p class=Normal> “Bhikkhus, eu não digo que todos os bhikkhus ainda têm trabalho a ser feito com diligência com relação às seis bases para o contato; nem eu digo que todos os bhikkhus não têm mais trabalho a ser feito com diligência com relação às seis bases para o contato.</p>

<p class=Normal>"Eu não digo que aqueles bhikkhus que são Arahants, com as impurezas destruídas, que viveram a vida santa, fizeram o que devia ser feito, depuseram o fardo, alcançaram o verdadeiro objetivo, destruíram os grilhões da existência e estão completamente libertados através do conhecimento supremo, ainda tenham trabalho a ser feito com diligência em relação às seis bases para o contato. Por que isso? Eles fizeram o seu trabalho com diligência; eles são incapazes de serem negligentes. </p>

<p class=Normal>"Mas eu digo que aqueles bhikkhus que são treinandos, que ainda não alcançaram o ideal, que permanecem aspirando pela suprema libertação do cativeiro, ainda têm trabalho a ser feito com diligência em relação às seis bases para o contato. Por que isso? Porque, bhikkhus, existem formas conscientizadas através do olho, que são agradáveis e aquelas que são desagradáveis. Ele deveria treinar de modo que estas não persistam obcecando a mente mesmo quando sejam experienciadas repetidamente. Quando a mente não está obcecada, a energia incansável é desperta, a atenção plena sem confusão é estabelecida, o corpo fica tranquilo e a mente se torna concentrada e unificada. Vendo esse fruto da diligência, bhikkhus, eu digo que esses bhikkhus ainda têm trabalho a ser feito com diligência em relação às seis bases para o contato .</p>

<p class=Normal>Existem sons conscientizados através do ouvido ... objetos mentais conscientizados através da mente, que são agradáveis e aqueles que são desagradáveis. Ele deveria treinar de modo que estes não persistam obcecando a mente mesmo quando sejam experienciados repetidamente. Quando a mente não está obcecada, a energia incansável é desperta, a atenção plena sem confusão é estabelecida, o corpo fica tranquilo e a mente se torna concentrada e unificada. Vendo esse fruto da diligência, bhikkhus, eu digo que esses bhikkhus ainda têm trabalho a ser feito com diligência em relação às seis bases para o contato."</p>


