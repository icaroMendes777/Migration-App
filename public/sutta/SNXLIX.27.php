<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus, como
um pote que tenha sido virado de cabeça para baixo abandona toda a sua água e
não a toma de volta, assim também um bhikkhu que desenvolve e cultiva os quatro
esforços corretos abandona todos os estados ruins e prejudiciais e não os toma
de volta. [<a href="#N1">1</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“E como um bhikkhu faz isso? Neste caso, bhikkhus, um bhikkhu gera
desejo de que não surjam estados ruins e prejudiciais que ainda não surgiram e
ele se aplica, estimula a sua energia, empenha a sua mente e se esforça. Ele
gera desejo de abandonar estados ruins e prejudiciais que já surgiram ... Ele
gera desejo de que surjam estados benéficos que ainda não surgiram ... Ele gera
desejo para a continuidade, o não desaparecimento, o fortalecimento, o
incremento e a realização através do desenvolvimento de estados benéficos que
já surgiram e ele se aplica, estimula a sua energia, empenha a sua mente e se
esforça. É dessa forma, bhikkhus, que um bhikkhu desenvolve e cultiva os quatro
esforços corretos para ele abandonar todos os estados ruins e prejudiciais e
não os tomar de volta.”<o:p></o:p></span></p>

