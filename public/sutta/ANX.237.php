<p class=Normal> (237) "Bhikkhus, para o conhecimento direto da cobiça, dez coisas devem ser desenvolvidas. Quais dez? (1) A percepção do repulsivo, (2) a percepção da morte, (3) a percepção das coisas repulsivas na comida, (4) a percepção do não-deleite com tudo no mundo, (5) a percepção da impermanência, (6) a percepção do sofrimento na impermanência, (7) a percepção de não-eu naquilo que é sofrimento, (8) a percepção do abandono, (9) a percepção do desapego, e (10) a percepção da cessação. Para o conhecimento direto da cobiça, essas dez coisas devem ser desenvolvidas." </p>

<p class=Normal> (238) "Bhikkhus, para o conhecimento direto da cobiça, dez coisas devem ser desenvolvidas. Quais dez? (1) A percepção da impermanência, (2) a percepção de não-eu, (3) a percepção das coisas repulsivas na comida, (4) a percepção de não-deleite com tudo no mundo, (5) a percepção do esqueleto, (6) a percepção de um cadáver infestado de vermes, (7) a percepção de um cadáver lívido, (8) a percepção de um cadáver supurando, (9) a percepção de um cadáver fissurado, e (10) a percepção de um cadáver inchado. Para o conhecimento direto da cobiça, essas dez coisas devem ser desenvolvidas." </p>

<p class=Normal> (239) "Bhikkhus, para o conhecimento direto da cobiça, dez coisas devem ser desenvolvidas. Quais dez? Entendimento correto, pensamento correto, linguagem correta, ação correta, modo de vida correto, esforço correto, atenção plena correta, concentração correta, conhecimento correto, libertação correta. Para o conhecimento direto da cobiça, essas dez coisas devem ser desenvolvidas." </p>

<p class=Normal> (240-266) "Bhikkhus, para a completa compreensão da cobiça ... para a completa destruição ... para o abandono ... para a destruição ... para o desaparecimento ... para o desapego ... para a cessação .... para abrir mão ... para a renúncia da cobiça, essas dez coisas devem ser desenvolvidas." </p>

<p class=Normal> (267-746) "Bhikkhus, para o conhecimento direto ... para a completa compreensão .... para a completa destruição ... para o abandono ... para a destruição ... para o desaparecimento ... para o desapego ... para a cessação .... para abrir mão ... para a renúncia da raiva ... da delusão ... do ódio ... da hostilidade ... da difamação ... da insolência ... da inveja ... da avareza ... da enganação ... da astúcia ... da teimosia .... da veemência ... da presunção ... da arrogância ... da embriaguez .... da negligência, essas dez coisas devem ser desenvolvidas." </p>


