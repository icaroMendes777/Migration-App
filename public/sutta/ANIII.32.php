<p class=Normal> "Então o Venerável Ananda foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse:

<p class=Normal><a name=R1></a>"Venerável Senhor, um bhikkhu poderia obter um estado de concentração no qual (1) não haveria a fabricação de um "eu", a fabricação do "meu" e a tendência subjacente à presunção em relação a este corpo dotado de consciência [<a href="#N1">1</a>]; (2) não haveria a fabricação de um "eu", a fabricação do "meu" e a tendência subjacente à presunção em relação a todos objetos externos; e (3) ele entraria e permaneceria na libertação da mente e libertação através da sabedoria através das quais não mais há a fabricação de um "eu", a fabricação do "meu" e a tendência subjacente à presunção para quem nelas penetra e permanece?" </p>

<p class=Normal>"Ele poderia, Ananda." </p>

<p class=Normal>"Mas como, venerável senhor, ele poderia obter tal estado de concentração?" </p>

<p class=Normal>"Aqui, Ananda, um bhikkhu considera: "Isto é pacífico, isto é sublime, o cessar de todas as formações, o abandono de todas aquisições, o fim do desejo, desapego, cessação, Nibbana.' Desse modo, Ananda, um bhikkhu poderia obter um estado de concentração no qual não haveria a fabricação de um "eu", a fabricação do "meu" e a tendência subjacente à presunção em relação a este corpo dotado de consciência; não haveria a fabricação de um "eu", a fabricação do "meu" e a tendência subjacente à presunção em relação a todos objetos externos; e ele entraria e permaneceria na libertação da mente e libertação através da sabedoria através das quais não mais há a fabricação de um "eu", a fabricação do "meu" e a tendência subjacente à presunção para quem nelas penetra e permanece. E foi com referência a isso que eu disse no Parayana-Vagga, na Perguntas de Punnaka: [<a href="#N2">2</a>]</p>

<p class=Normal>"Aquele que compreendeu no mundo o aqui e o além, em quem não há perturbação por nada no mundo, que está tranqüilo, livre das chamas ardentes, despreocupado e livre do desejo – ele superou o nascimento e o envelhecimento, eu digo.”</p>


