<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Certa vez, bhikkhus, em
Varanasi, Brahmadatta era o rei de Kasi - rico, próspero, com muitas posses,
muitas tropas, muitos veículos, muitos territórios, com os arsenais e celeiros
abundantemente estocados. Dighiti era o rei de Kosala - pobre, não muito
próspero, com poucas posses, poucas tropas, poucos veículos, poucos
territórios, com os arsenais e celeiros pobremente estocados. Assim Brahmadatta
o rei de Kasi recrutando um exército quadruplicado, marchou contra Dighiti o
rei de Kosala. Dighiti o rei de Kosala ouviu, &quot;Brahmadatta o rei de Kasi,
dizem, recrutou um exército quadruplicado e está marchando contra mim.&quot;
Então o pensamento lhe ocorreu, &quot;O rei Brahmadatta é rico, próspero...com
os arsenais e celeiros abundantemente estocados, enquanto que eu sou
pobre...com arsenais e celeiros pobremente estocados. Eu não tenho competência
para resistir nem um ataque dele. Porque eu não escapo da cidade antes?&quot;
Assim, tomando a sua primeira esposa, ele escapou da cidade antes. Então o rei
Brahmadatta, tendo conquistado as tropas, veículos, terras, arsenais e celeiros
do rei Dighiti, viveu como o seu senhor. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Enquanto isso, o rei
Dighiti dirigiu-se para Varanasi juntamente com a sua consorte e, viajando em
etapas chegou lá. Lá ele viveu com ela nos arredores de Varanasi na casa de um
oleiro, disfarçado como um errante. Não muito tempo depois, ela engravidou. Ela
tinha um desejo de grávida deste tipo: ela queria ver um exército
quadruplicado, armado e formado, em posição em um campo de desfiles ao
amanhecer, e de beber a água usada para lavar as espadas. Ela disse ao rei
Dighiti, &quot;Sua majestade, eu estou grávida, e tenho um desejo de grávida
deste tipo: eu quero ver um exército quadruplicado, armado e formado, em posição
em um campo de desfiles ao amanhecer, e de beber a água usada para lavar as
espadas.&quot; Ele disse, &quot; Minha rainha, aonde está para nós - que
perdemos nossa grandeza - um exército quadruplicado, armado e formado, em
posição em um campo de desfiles ao amanhecer, e a água usada para lavar as
espadas?&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Se eu não obtiver
isso, sua majestade, morrerei.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Agora, naquela época o
brâmane conselheiro do rei Brahmadatta era uma migo do rei Dighiti. Assim o rei
Dighiti o procurou e, chegando, disse, &quot; Uma senhora, sua amiga, velha
amiga, está grávida, e ela tem um desejo de gravidez deste tipo: ela quer ver
um exército quadruplicado, armado e formado, em posição em um campo de desfiles
ao amanhecer, e de beber a água usada para lavar as espadas.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Nesse caso, deixe-me
vê-la.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim a consorte do rei
Dighiti foi até o brâmane conselheiro do rei Brahmadatta. Quando ele a viu
vindo `a distância, ele se levantou do seu assento, arrumou o seu manto sobre
um ombro e, com as suas mãos erguidas em saudação, exclamou três vezes,
&quot;Com certeza o rei de Kosala veio ao seu ventre! Com certeza o rei de
Kosala veio ao seu ventre! Não se preocupe minha rainha. Você verá um exército
quadruplicado, armado e formado, em posição em um campo de desfiles ao
amanhecer, e de beber a água usada para lavar as espadas.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então ele foi até o rei
Brahmadatta e, chegando, lhe disse, &quot;Sua majestade, sinais apareceram de
que amanhã ao amanhecer um exército quadruplicado, armado e formado, deve estar
em posição em um campo de desfiles e que as espadas devem ser lavadas.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim o rei Brahmadatta
ordenou ao seu povo, &quot;eu lhes digo então: façam como diz o conselheiro
brâmane&quot;. Assim a consorte do rei Dighiti viu um exército quadruplicado,
armado e formado, em posição em um campo de desfiles ao amanhecer, e de beber a
água usada para lavar as espadas. Então com o amadurecimento do feto, ela deu à
luz um menino, que eles deram o nome de Dighavu (vida longa). Não muito tempo
depois, o Príncipe Dighavu atingiu a idade da discrição. O pensamento ocorreu ao
rei Dighiti, &quot; Esse rei Brahmadatta de Kasi nos fez um grande mal. Ele
tomou nossas tropas, veículos, terras, arsenais e celeiros. Se ele nos
descobre, ele matará os três. Porque não mando o Príncipe Dighavu para viver
fora da cidade?&quot; Assim o Príncipe Dighavu, tendo ido viver fora da cidade,
aprendeu todos os ofícios. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Agora, nessa época o
barbeiro do rei Dighiti foi até o rei Brahmadatta. Ele viu o rei Dighiti,
juntamente com a sua consorte, vivendo nos arredores de Varanasi na casa de um
oleiro, disfarçado como um errante. Vendo-os, ele foi até o rei Brahmadatta e,
chegando, lhe disse, <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Sua majestade, o rei
Dighiti de Kosala, juntamente com sua consorte, está vivendo nos arredores de
Varanasi na casa de um oleiro, disfarçado como um errante.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim o rei Brahmadatta
ordenou a seu povo, &quot;Eu então digo: tragam-me o rei Dighiti juntamente com
a sua consorte.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Respondendo, &quot;Assim
será, sua majestade,&quot; eles foram buscar e trouxeram o rei Dighiti
juntamente com sua consorte. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o rei Brahmadatta
ordenou ao seu povo, &quot;Eu então digo: tendo amarrado o rei Dighiti e a sua
consorte com uma corda forte com os seus braços presos com força às suas
costas, e tendo raspado suas cabeças, leve-os em desfile ao som forte de um
tambor pungente de rua em rua, cruzamento em cruzamento, expulse-os pela porta
sul da cidade e ali, ao sul da cidade, corte-os em quatro pedaços e enterre-os
em buracos posicionados nas quatro direções.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Respondendo, &quot;Assim
será, sua majestade,&quot; o povo amarrou o rei Dighiti e a sua consorte com
uma corda forte com os seus braços presos com força às suas costas, e tendo
raspado suas cabeças, levou-os em desfile ao som de um tambor pungente de rua
em rua, cruzamento em cruzamento. Então o pensamento ocorreu ao Príncipe
Dighavu, &quot; Já faz muito tempo que não vejo minha mãe e meu pai. E se eu
fosse visitá-los?&quot; Assim ele entrou em Varanasi e viu sua mãe e pai
amarrados com uma corda forte com os braços presos com força às costas, com as
cabeças raspadas, levados em desfile ao som de um tambor pungente de rua em
rua, cruzamento em cruzamento. Assim ele se dirigiu até eles. O rei Dighiti viu
o Príncipe Dighavu vindo `a distância, e vendo-o, disse, ' Não meu querido
Dighavu, tenha a visão longa. Não tenha a visão curta. Pois a vingança não se
resolve com vingança. A vingança se resolve com a não vingança.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isto foi dito, as
pessoas lhe disseram, &quot;Esse rei Dighiti enlouqueceu. Ele está falando
besteiras. Quem é Dighavu? Porque ele está dizendo, ' Não meu querido Dighavu,
tenha a visão longa. Não tenha a visão curta. Pois a vingança não se resolve
com vingança. A vingança se resolve com a não vingança.'?&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Eu não estou louco ou
falando besteiras. Aquele que sabe irá entender.&quot; Então ele falou uma
Segunda vez... e uma terceira vez &quot; Não meu querido Dighavu, tenha a visão
longa. Não tenha a visão curta. Pois a vingança não se resolve com vingança. A
vingança se resolve com a não vingança.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Uma terceira vez, o povo
lhe disse, &quot;Esse rei Dighiti enlouqueceu. Ele está falando besteiras. Quem
é Dighavu? Porque ele está dizendo, ' Não meu querido Dighavu, tenha a visão
longa. Não tenha a visão curta. Pois a vingança não se resolve com vingança. A
vingança se resolve com a não vingança.'?&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Eu não estou louco ou
falando besteiras. Aquele que sabe irá entender.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o povo tendo
desfilado o rei Dighiti juntamente com sua consorte ao som forte de um tambor
pungente de rua em rua, cruzamento em cruzamento, os expulsaram pela porta sul
da cidade e ali, ao sul da cidade, os cortaram em quatro pedaços e os enterraram
em buracos posicionados nas quatro direções, colocaram guardas, e
partiram&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o Príncipe Dighavu,
tendo entrado em Varanasi apareceu com uma bebida alcoólica e fez com que os
guardas a tomassem. Quando eles caíram bêbados, ele juntou paus, fez uma pira,
ergueu os corpos da sua mãe e de seu pai sobre a pira, colocou fogo, e então
circundou-a três vezes com suas mãos erguidas em saudação. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Agora, naquele momento, o
rei Brahmadatta havia subido ao terraço no topo do seu palácio. Ele viu o
Príncipe Dighavu circundando a pira três vezes com as mãos erguidas em
saudação, e vendo-o, o pensamento lhe ocorreu, &quot;Sem dúvida essa pessoa é
um parente do rei Dighiti. Ah! que desafortunado eu sou, pois não há ninguém
que me possa dizer o que isso significa!&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o Príncipe Dighavu,
tendo ido para a floresta e tendo gritado e chorado o quanto ele necessitava,
enxugou suas lágrimas e entrou em Varanasi. Indo ao estábulo de elefantes
próximo ao palácio do rei, ele disse ao treinador chefe de elefantes, &quot;Mestre,
eu quero aprender esse ofício.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Nesse caso, jovem,
você pode aprendê-lo.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, levantando-se na
última vigília da noite, o Príncipe Dighavu cantava com uma voz doce e tocava o
alaúde no estábulo dos elefantes. O rei Brahmadatta, também levantando-se na última
vigília da noite, ouviu a doce voz cantando e o alaúde tocando no estábulo dos
elefantes. Ouvindo, ele perguntou a seu povo, &quot;Eu digo: Quem é esse, que
levanta na última vigília da noite, canta com um voz doce e toca alaúde no
estábulo dos elefantes?&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Sua majestade, um
jovem - o aluno de tal e tal treinador de elefantes, levantando-se na última
vigília da noite, estava cantando com uma voz doce e tocando o alaúde no
estábulo dos elefantes.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Eu então digo: vá e
traga-me esse jovem.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Respondendo, &quot;Assim
será, sua majestade,&quot; eles foram e trouxeram o Príncipe Dighavu. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o rei Brahmadatta
disse ao Príncipe Dighavu, &quot;Eu digo: Foi você que levantou na última
vigília da noite, cantando com uma voz doce e tocando o alaúde no estábulo dos
elefantes?&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Sim, sua
majestade&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Eu então digo meu
jovem: cante e toque o alaúde.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Respondendo, &quot;Assim
será, sua majestade,&quot; e buscando obter um favor, o Príncipe Dighavu cantou
com uma voz doce e tocou o alaúde. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o rei Brahmadatta lhe
disse, &quot;Eu digo: Você, meu jovem, deve ficar e ocupar-se comigo.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Assim será, sua
majestade,&quot; o Príncipe Dighavu respondeu. Então ele se levantava pela
manhã antes do rei Brahmadatta, ia para a cama pela noite depois dele, fazia
tudo o que o rei ordenava, sempre agindo para agradá-lo, falando com ele com
cortesia. E não tardou muito para que o rei Brahmadatta colocasse o príncipe
próximo a ele em uma posição de confiança. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então um dia o rei
Brahmadatta disse ao Príncipe Dighavu, &quot;Eu então digo, meu jovem: arreie a
carruagem. Eu irei caçar.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Respondendo: &quot;Assim
será, sua majestade,&quot; o Príncipe Dighavu arreou a carruagem e então disse
ao rei Brahmadatta, &quot;Sua carruagem está arreada, sua majestade. Agora é o
momento de você agir como julgar mais adequado.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o rei Brahmadatta
montou na carruagem, e o Príncipe Dighavu a conduziu. Ele a conduziu de tal
forma que o séquito do rei foi em uma direção e a carruagem em outra. Então
após eles terem percorrido alguma distância o rei Brahmadatta disse ao Príncipe
Dighavu, &quot;Eu então lhe digo, meu jovem: desarreie a carruagem. Eu estou
cansado e irei me deitar.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Respondendo, &quot;Assim
será, sua majestade,&quot; o Príncipe Dighavu desarreou a carruagem e sentou-se
de pernas cruzadas no chão. Então o rei Brahmadatta deitou-se, colocando sua
cabeça sobre o colo do Príncipe Dighavu. Como ele estava cansado, ele dormiu
imediatamente. Então o pensamento ocorreu ao Príncipe Dighavu: &quot;Este rei
Brahmadatta de Kasi nos fez um grande mal. Ele tomou nossas tropas, veículos,
terras, arsenais e celeiros. E foi por sua causa que minha mãe e pai forma
mortos. Agora é a minha oportunidade para por em prática a minha
vingança!&quot; Ele puxou a espada da bainha. Mas então ele pensou, &quot;Meu
pai me disse, quando ele estava a ponto de morrer, ' Não meu querido Dighavu,
tenha a visão longa. Não tenha a visão curta. Pois a vingança não se resolve
com vingança. A vingança se resolve com a não vingança.' Não me seria adequado
transgredir as palavras do meu pai.&quot; Assim ele embainhou a espada. Uma
Segunda vez...uma terceira vez o pensamento ocorreu ao Príncipe Dighavu:
&quot;Este rei Brahmadatta de Kasi nos fez um grande mal. Ele tomou nossas
tropas, veículos, terras, arsenais e celeiros. E foi por sua causa que minha
mãe e pai forma mortos. Agora é a minha oportunidade para por em prática a
minha vingança!&quot; Ele puxou a espada da bainha. Mas então ele pensou,
&quot;Meu pai me disse, quando ele estava a ponto de morrer, ' Não meu querido
Dighavu, tenha a visão longa. Não tenha a visão curta. Pois a vingança não se
resolve com vingança. A vingança se resolve com a não vingança.' Não me seria
adequado transgredir as palavras do meu pai.&quot; Assim uma vez mais ele
embainhou a espada. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então de repente o rei
Brahmadatta acordou - assustado, agitado, com medo, alarmado. O Príncipe
Dighavu lhe disse, &quot;Sua majestade, porque você acordou tão de repente -
assustado, agitado, com medo, alarmado?&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Eu digo, meu jovem:
agora mesmo eu estava sonhando que o Príncipe - filho de Dighiti, rei de Kasi -
me golpeou com uma espada.&quot; Então o Príncipe Dighavu, agarrando o rei
Brahmadatta pela cabeça com a sua mão esquerda, e desembainhando a espada com a
direita, disse &quot;Eu, sua majestade, sou o Príncipe Dighavu, filho de
Dighiti, rei de Kasi. Você nos fez um grande mal. Você tomou nossas tropas,
veículos, terras, arsenais e celeiros. E foi por sua causa que minha mãe e pai
foram mortos. Agora é a minha oportunidade para por em prática a minha
vingança!&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim o rei Brahmadatta,
deixando sua cabeça cair aos pés do Príncipe Dighavu disse, <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Conceda-me minha
vida, estimado Dighavu! Conceda-me minha vida, estimado Dighavu!&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Quem sou eu que
ousaria conceder a vida para sua majestade? É sua majestade que deveria
conceder-me a vida!&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Nesse caso estimado
Dighavu, você me concede a minha vida e eu lhe concedo a sua vida.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o rei Brahmadatta e o
Príncipe Dighavu concederam-se uma ao outro as suas vidas, e, tomando-se as
mãos, e fizeram um juramento de não fazer dano um ao outro. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o rei Brahmadatta
disse ao Príncipe Dighavu, &quot;Nesse caso, estimado Dighavu, arreie a carruagem.
Nós seguiremos.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Respondendo, &quot;Assim
será, sua majestade,&quot; o Príncipe Dighavu arreou a carruagem e então disse
ao rei Brahmadatta, &quot;Sua carruagem está arreada, sua majestade. Agora é o
momento de você agir como considerar mais adequado.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o rei Brahmadatta
montou na carruagem, e o Príncipe conduziu a carruagem. Ele a conduziu de tal
forma que não tardou muito tempo eles se encontraram com o séquito do rei. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o rei Brahmadatta,
tendo entrado em Varanasi, reuniu-se com os seus ministros e conselheiros e
lhes disse, &quot;Então, eu lhes digo. Se vocês vissem o Príncipe Dighavu, o
filho de Dighiti, o rei de Kasi, o que vocês fariam com ele?&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Diferentes ministros
disseram, &quot;Nós cortaríamos as suas mãos, sua majestade&quot; - &quot;Nós
cortaríamos os seus pés, sua majestade&quot; - &quot;Nós cortaríamos as suas
mãos e os seus pés, sua majestade&quot; - &quot;Nós cortaríamos as suas
orelhas, sua majestade&quot; - &quot;Nós cortaríamos o seu nariz, sua
majestade&quot; - &quot;Nós cortaríamos as suas orelhas e o seu nariz, sua
majestade&quot; - &quot;Nós cortaríamos a sua cabeça, sua majestade.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o rei disse,
&quot;Este, eu digo, é o Príncipe Dighavu, o filho de Dighiti, o rei de Kasi.
Vocês não têm permissão para fazer nada a ele. Foi por ele que a minha vida me
foi concedida, e foi por mim que a sua vida lhe foi concedida.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o rei Brahmadatta
disse ao Príncipe Dighavu, &quot;O que o seu pai lhe disse quando ele estava a
ponto de morrer - ' Não meu querido Dighavu, tenha a visão longa. Não tenha a
visão curta. Pois a vingança não se resolve com vingança. A vingança se resolve
com a não vingança.' - em referência a o que ele disse isso?&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;O que meu pai me
disse quando ele estava a ponto de morrer - 'Não tenha a visão longa' - 'Não
sustente a vingança por um longo tempo' é o que ele estava me dizendo quando ele
estava a ponto de morrer. E o que ele me disse quando estava a ponto de morrer
- 'Não tenha a visão curta' - 'Não seja rápido para romperr com um amigo' é o
que ele me disse quando estava aponto de morrer. E o que ele me disse quando
estava a ponto de morrer - 'Pois a vingança não se resolve através da vingança.
A vingança se resolve através da não vingança' - minha mãe e pai foram mortos
por sua majestade. Se eu tirasse a vida de vossa majestade, aqueles que desejam
o bem estar de sua majestade iriam tirar-me a vida. E aqueles que desejam o meu
bem estar iriam tirar-lhes a vida. E dessa forma a vingança não seria resolvida
pela vingança. Porém agora me foi concedida a minha vida por sua majestade, e
sua majestade teve a sua vida concedida por mim. E dessa forma a vingança foi
resolvida com a não vingança. Isso foi o que meu pai me disse quando estava
aponto de morrer.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o rei Brahmadatta
disse, &quot;Não é impressionante! Não é espantoso! Quão sábio é esse Príncipe Dighavu,
pois ele pode entender o sentido completo daquilo que seu pai lhe disse de
maneira breve!&quot; Assim ele devolveu as tropas, veículos, terras, arsenais e
celeiros do seu pai, e lhe deu sua filha em casamento. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim, bhikkhus, é a
indulgência e a nobreza de reis que utilizam com destreza o cetro, que utilizam
com destreza a espada. Agora permitam que a sua luz brilhe, para que vocês -
que tomaram a vida santa nesse tão bem ensinado Dhamma e Disciplina - se
igualem a eles na indulgência e na nobreza. <o:p></o:p></span></p>


