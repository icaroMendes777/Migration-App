<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
há essas cinco faculdades. Quais cinco? A faculdade da convicção, a faculdade
da energia, a faculdade da atenção plena, a faculdade da concentração, a
faculdade da sabedoria. Essas são as cinco faculdades.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aquele que
completou e realizou essas cinco faculdades é um arahant. Se elas forem mais
fracas do que isso, ele está praticando para realizar o fruto de arahant; se
elas forem mais fracas do que isso, ele é um que não retorna; se elas forem
mais fracas do que isso, ele está praticando para realizar o fruto do que não
retorna; se elas forem mais fracas do que isso, ele é um que retorna uma vez;
se elas forem mais fracas do que isso, ele está praticando para realizar o
fruto do que retorna uma vez; se elas forem mais fracas do que isso, ele é um
que entrou na correnteza; se elas forem mais fracas do que isso, ele está
praticando para realizar o fruto do que entrou na correnteza.<o:p></o:p></span></p>

<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Mas, bhikkhus, eu digo que aquele em quem essas cinco faculdades estão
completamente e totalmente ausentes é um forasteiro, alguém que faz parte da
facção dos mundanos.” [<a href="#N1">1</a>] <o:p></o:p></span></p>

