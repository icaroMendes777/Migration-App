<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em Savathi.
”Bhikkhus, suponham que existisse um rio, correndo montanha abaixo, indo longe,
com uma rápida correnteza. Se em ambas margens houvesse vários tipos de capim,
arbustos e árvores crescendo, eles se projetariam sobre o rio. Se um homem,
arrastado pela correnteza, se agarrasse ao capim, este se partiria e por
essa razão o homem daria de encontro com a calamidade e o desastre. Se ele se agarrasse
aos arbustos ... aos galhos das arvores, estes se partiriam e por essa razão o homem daria
de encontro com a calamidade e o desastre. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Assim
também, bhikkhus, uma pessoa comum sem instrução que não respeita os nobres,
que não é proficiente nem treinada no Dhamma deles, que não respeita os
homens verdadeiros, que não é proficiente nem treinada no Dhamma deles –
supõe que a forma seja o eu, ou o eu possuído de forma, ou a forma como estando
no eu, ou o eu como estando na forma. Essa forma se desintegra e assim ela daria
de encontro com a calamidade e o desastre.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ela supõe
