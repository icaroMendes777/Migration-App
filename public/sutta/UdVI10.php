<p class=Normal>Assim ouvi. Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no Parque de Anathapindika.</p>



<p class=Normal>Então o venerável Ananda foi até o Abençoado e, após cumprimentá-lo, sentou a um lado e disse: "Senhor, é quando não surgem no mundo Tathagatas - arahants, perfeitamente iluminados - que errantes de outras seitas são honrados, respeitados, reverenciados e venerados - obtendo mantos, comida esmolada, moradias e medicamentos. Mas é quando surgem no mundo Tathagatas - arahants, perfeitamente iluminados - que errantes de outras seitas não são honrados, respeitados, reverenciados e venerados - não obtendo mantos, comida esmolada, moradias e medicamentos. Agora apenas o Abençoado, juntamente com a comunidade de bhikkhus - é honrado, respeitado, reverenciado e venerado - obtendo mantos, comida esmolada, moradias e medicamentos.”</p>



<p class=Normal>"É assim, Ananda. É assim. "É quando não surgem no mundo Tathagatas - arahants, perfeitamente iluminados - que errantes de outras seitas são honrados, respeitados, reverenciados e venerados - obtendo mantos, comida esmolada, moradias e medicamentos. Mas é quando surgem no mundo Tathagatas - arahants, perfeitamente iluminados - que errantes de outras seitas não são honrados, respeitados, reverenciados e venerados - não obtendo mantos, comida esmolada, moradias e medicamentos. Agora apenas o Abençoado, juntamente com a comunidade de bhikkhus - é honrado, respeitado, reverenciado e venerado - obtendo mantos, comida esmolada, moradias e medicamentos.”</p>



<p class=Normal>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p>



	<p class=Normal style='margin-left:.75in'> "Os vaga-lumes apenas brilham<br>

	enquanto o sol ainda não nasceu.<br>

	Mas é quando se dá o raiar do sol<br>

	com todo o seu esplendor,<br>

	que a luz dos vaga-lumes desaparece,<br>

	é destruída.<br>

	Da mesma forma, os errantes sectários apenas brilham<br>

	enquanto não surgiram aqueles perfeitamente  iluminados.<br>

	Tais filósofos não são puros,<br>

	tampouco seus discípulos.<br>

	Aqueles dedicados ao entendimento incorreto<br>

	não encontram o fim do sofrimento.”</p>

