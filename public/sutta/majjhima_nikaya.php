<p class="Normal"><span lang=PT-BR mso-ansi-language:PT-BR'>Um conjunto de notas explicativas foi incluido no final dos suttas para esclarecer passagens mais complexas ou para ampliar o significado de alguns trechos cujo conteúdo é mais rico do que parece à primeira vista. Muitas dessas notas foram extraidas dos comentários ao Majjhima Nikaya. Existem na realidade dois comentários, sendo que o primeiro é o comentário propriamente dito, denominado<i> Majjhima Nikaya Atthakatha</i>, composto no século quinto pelo eminente comentarista Budista Acariya Buddhaghosa, que se baseou em antigos registros (que já não estão dsiponíveis) que haviam sido preservados ao longo de séculos pela Sangha do Monastério de Mahavihara em Anuradhapura no Sri Lanka. Os comentários são úteis não somente para elucidar o significado dos textos, mas também para esclarecer o contexto dos eventos que resultaram na promulgação dos discursos. O segundo comentário é um sub-comentário, denominado <i>Majjhima Nikaya Tika</i>, atribuido a Acariya Dhammapala que provavelmente viveu e trabalhou no sul da Índia um século ou mais depois de Acariya Buddhagosa. O principal objetivo do Tika é esclarecer passagens obscuras ou complexas contidas no Atthakatha.</span></p>
