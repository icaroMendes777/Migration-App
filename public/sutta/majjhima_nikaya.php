<p class=Tit1 align=center style='text-align:center'>Majjhima Nikaya</p>

<p class=Tit2 align=center style='text-align:center'>Os Discursos com Extensão Média</p>

<hr size=1 width="100%" align=center>

<p class="Normal"><span lang=PT-BR mso-ansi-language:PT-BR'>O Majjhima Nikaya, ou "Discursos com Extensão Média", 
é o segundo dos cinco<i> nikayas</i>, ou coleções, do  Sutta Pitaka. Este nikaya consiste de 152 discursos, que 
se destaca dos demais livros do Sutta Pitaka por ser uma coleção que combina uma rica variedade de contextos 
em que os discursos foram proferidos com um conjunto profundo e abrangente de ensinamentos. Os suttas estão agrupados 
em três partes e cada parte em <i>vaggas</i>, com 10 suttas em cada <i>vagga</i>:</span></p>


 <p class=Normal style='margin-left:.75in'><b> <i>Mulapannasapali</i></b> - Primeira Parte: Os Cinqüenta Primeiros Discursos </span><br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<i><a href="#mula">Mulapariyayavagga</a></i> (MN 1 a MN 10)<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<i><a href="#siha">Sihanadavagga</a></i>     (MN 11 a MN 20)<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<i><a href="#tati">Tatiyavagga</a></i>       (MN 21 a MN 30)<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<i><a href="#maha">Mahayamakavagga</a></i> (MN 31 a MN 40)<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<i><a href="#cula">Culayamakavagga</a></i> (MN 41 a MN 50)</p>

