<p class=Normal> “Bhikkhus, essas cinco coisas quando desenvolvidas e cultivadas têm a libertação da mente como seu fruto, a libertação da mente como seu fruto e benefício; têm a libertação através da sabedoria como seu fruto, a libertação através da sabedoria como seu fruto e benefício. Quais cinco? Aqui, um bhikkhu permanece contemplando as coisas repulsivas no corpo, percebendo as coisas repulsivas na comida, percebendo o não-deleite com tudo no mundo, contemplando a impermanência em todas as formações condicionadas; e ele tem a percepção da morte bem estabelecida internamente. Essas cinco coisas quando desenvolvidas e cultivadas têm a libertação da mente como seu fruto, a libertação da mente como seu fruto e benefício; têm a libertação através da sabedoria como seu fruto, a libertação através da sabedoria como seu fruto e benefício. </p>

<p class=Normal><a name=R1></a>  "Quando um bhikkhu está com a mente libertada e libertado através da sabedoria, ele é chamado um bhikkhu cuja haste foi levantada, cuja vala foi preenchida, cujo pilar foi desenraizado, aquele que não possui ferrolho, um nobre cuja bandeira está abaixada, cujo fardo foi deposto, aquele que não está aprisionado. [<a href="#N1">1</a>]  </p>

<p class=Normal> “E como é o bhikkhu, aquele cuja haste foi levantada? Aqui, o bhikkhu abandonou a ignorância, cortou-a pela raiz, fez como com um tronco de palmeira eliminando-a de tal forma que não estará mais sujeita a um futuro surgimento. Assim é o bhikkhu, aquele cuja haste foi levantada. </p>

<p class=Normal> “E como é o bhikkhu, aquele cuja vala foi preenchida? Aqui, o bhikkhu abandonou o ciclo de renascimentos que traz a renovação do ser/existir, cortou-o pela raiz ... não estará mais sujeito a um futuro surgimento. Assim é o bhikkhu, aquele cuja vala foi preenchida. </p>

<p class=Normal> “E como é o bhikkhu, aquele cujo pilar foi desenraizado? Aqui, o bhikkhu abandonou o desejo, cortou-o pela raiz ... não estará mais sujeito a um futuro surgimento. Assim é o bhikkhu, aquele cujo pilar foi desenraizado. </p>

<p class=Normal> “E como é o bhikkhu, aquele que não possui ferrolho? Aqui o bhikkhu abandonou os cinco primeiros grilhões, cortou-os pela raiz ... não estarão mais sujeitos a um futuro surgimento. Assim é o bhikkhu, aquele que não possui barreiras. </p>

<p class=Normal> “E como é o bhikkhu, que é um nobre, cuja bandeira está abaixada, cujo fardo foi deposto, que não está aprisionado? Aqui, o bhikkhu abandonou a presunção ‘Eu sou’, cortou-a pela raiz ... não estará mais sujeita a um futuro surgimento. Assim é o bhikkhu, que é um nobre, cuja bandeira está abaixada, cujo fardo foi deposto, que não está aprisionado. </p>

