<p class=Normal> Então um certo bhikkhu foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse: </p>

<p class=Normal> (1) "Venerável senhor, o que conduz o mundo em círculos? Através do que ele é arrastado para cá e para lá? Qual é aquela coisa que tem tudo sob seu controle? </p>

<p class=Normal>"Muito bem bhikkhu, a sua perspicácia é excelente. O seu discernimento é excelente. A sua pergunta é muito boa. Pois você assim perguntou: 'O que conduz o mundo em círculos? Através do que ele é arrastado para cá e para lá? Qual é aquela coisa que tem tudo sob seu controle?'"</p>

<p class=Normal>"Sim, venerável senhor." </p>

<p class=Normal>"O mundo, bhikkhu, é conduzido pela mente; pela mente ele é arrastado para cá e para lá. A mente é aquela coisa que tem tudo sob seu controle." </p>

<p class=Normal>Dizendo, "Muito bem venerável senhor", aquele bhikkhu ficou contente e satisfeito com a resposta do Abençoado. Depois ele fez uma outra pergunta: </p>

<p class=Normal> (2) "É dito, venerável senhor, 'um estudado experto no Dhamma, um estudado experto no Dhamma.' De que forma alguém é um estudado experto no Dhamma?" </p>

<p class=Normal>"Muito bem bhikkhu, a sua perspicácia é excelente. O seu discernimento é excelente. A sua pergunta é muito boa. Pois você assim perguntou: ' É dito, venerável senhor, 'um estudado experto no Dhamma, um estudado experto no Dhamma.' De que forma alguém é um estudado experto no Dhamma?'"</p>

<p class=Normal>"Sim, venerável senhor." </p>

<p class=Normal>"Eu dei muitos ensinamentos, bhikkhu: sumários, prosa e verso, análises, versos, exclamações, símiles, histórias de vidas passadas, eventos maravilhosos, perguntas e respostas. Se, após aprender o significado e o Dhamma de apenas um poema com quatro versos, ele praticar de acordo com o Dhamma, isso é o suficiente para alguém ser chamado 'um estudado experto no Dhamma.'"</p>

<p class=Normal>Dizendo, "Muito bem venerável senhor", aquele bhikkhu ficou contente e satisfeito com a resposta do Abençoado. Depois ele fez uma outra pergunta:

<p class=Normal> (3) "É dito, venerável senhor, 'um estudado com sabedoria penetrante, um estudado com sabedoria penetrante.' De que forma alguém é um estudado com sabedoria penetrante?" </p>

<p class=Normal>"Muito bem bhikkhu, a sua perspicácia é excelente. O seu discernimento é excelente. A sua pergunta é muito boa. Pois você assim perguntou: ' É dito, venerável senhor, 'um estudado com sabedoria penetrante, um estudado com sabedoria penetrante.' De que forma alguém é um estudado com sabedoria penetrante?'"</p>

<p class=Normal>"Sim, venerável senhor." </p>

<p class=Normal>"Aqui, bhikkhu, um bhikkhu ouviu: 'Isto é sofrimento,' e ele vê o significado disso tendo penetrado com a sabedoria. Ele ouve: 'Esta é a origem do sofrimento,' e ele vê o significado disso tendo penetrado com a sabedoria. Ele ouve: 'Esta é a cessação do sofrimento,' e ele vê o significado disso tendo penetrado com a sabedoria. Ele ouve: 'Este é o caminho que conduz à cessação do sofrimento,' e ele vê o significado disso tendo penetrado com a sabedoria. Desse modo alguém é um estudado com sabedoria penetrante." </p>

<p class=Normal>Dizendo, "Muito bem venerável senhor", aquele bhikkhu ficou contente e satisfeito com a resposta do Abençoado. Depois ele fez uma outra pergunta: </p>

<p class=Normal> (4) "É dito, venerável senhor, 'uma pessoa sábia com grande sabedoria, 'uma pessoa sábia com grande sabedoria.' De que forma alguém é 'uma pessoa sábia com grande sabedoria?" </p>

<p class=Normal>"Muito bem bhikkhu, a sua perspicácia é excelente. O seu discernimento é excelente. A sua pergunta é muito boa. Pois você assim perguntou: ' É dito, venerável senhor, 'uma pessoa sábia com grande sabedoria, 'uma pessoa sábia com grande sabedoria.' De que forma alguém é 'uma pessoa sábia com grande sabedoria?'"</p>

<p class=Normal>"Sim, venerável senhor." </p>

<p class=Normal>"Aqui, bhikkhu, uma pessoa sábia com grande sabedoria não intenciona por sua própria aflição, ou pela aflição dos outros, ou pela aflição de ambos. Ao invés disso, quando ela pensa, pensa apenas no seu bem-estar, no bem-estar dos outros, no bem-estar de ambos, e no bem-estar de todo o mundo. Desse modo alguém é 'uma pessoa sábia com grande sabedoria."</p>


