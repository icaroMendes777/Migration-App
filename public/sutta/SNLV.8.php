<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim ouvi.[<a href="#NA">A</a>] <a name=RA></a>
Em certa ocasião, o Abençoado estava em Natika na Casa de Tijolos. Então, o
Venerável Ananda foi até o Abençoado e depois de cumprimentá-lo sentou a um
lado e disse:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, o bhikkhu chamado Salha morreu. Qual é o seu destino, qual é o seu futuro
percurso? A bhikkhuni chamada Nanda morreu. Qual é o seu destino, qual é o seu
futuro percurso? O discípulo leigo chamada Sudatta morreu. Qual é o seu
destino, qual é o seu futuro percurso? O discípulo leigo chamado Sujata morreu.
Qual é o seu destino, qual é o seu futuro percurso?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ananda, o
bhikkhu Salha que morreu, com a eliminação das impurezas mentais permaneceu num
estado livre de impurezas com a libertação da mente e a libertação através da
sabedoria, tendo conhecido e manifestado isso para si mesmo no aqui e agora. A
bhikkhuni Nanda que morreu, com a destruição dos primeiros cinco grilhões,
reapareceu espontaneamente [nas Moradas Puras] e lá irá realizar o parinibbana 
sem nunca mais retornar daquele mundo. O discípulo leigo Sudatta que morreu, com
a destruição de três grilhões e com a atenuação da cobiça, raiva e delusão se
tornou um que retorna uma vez, retornando uma vez a este mundo para dar um fim ao sofrimento. [<a href="#N1">1</a>] <a name=R1></a>O
discípulo leigo Sujata, com a destruição de três grilhões, se tornou uma que entrou na correnteza, 
não mais destinada aos mundos inferiores, com o destino fixo, ela tem a
iluminação como destino.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não é
surpreendente, Ananda, que um ser humano morra. Mas se cada vez que alguém
morrer você vier me perguntar, isso criaria problemas para o Tathagata.
Portanto, Ananda, eu ensinarei para você uma exposição do Dhamma chamada o
espelho do Dhamma, através do qual um nobre discípulo, se ele assim
desejar, poderá por si próprio declarar de si mesmo: ‘Eu sou um daqueles que
deu fim ao inferno, fim ao reino animal, fim ao reino dos fantasmas, fim aos
planos de miséria, fim aos destinos ruins, fim aos mundos inferiores. Eu entrei
na correnteza, não mais destinado aos mundos inferiores, com o destino fixo, tenho
a iluminação como destino.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
Ananda, é essa exposição do Dhamma, um espelho do Dhamma, através do qual
um nobre discípulo, se ele assim desejar, poderá por si próprio declarar isso
de si mesmo? Aqui, Ananda, o nobre discípulo possui perfeita claridade, serenidade e confiança no
Buda assim: ‘O Abençoado é ... mestre de devas e humanos, desperto, sublime.’
Ele possui perfeita claridade, serenidade e confiança no Dhamma ... na Sangha ... Ele possui as
virtudes apreciadas pelos nobres – intactas ... que conduzem à concentração.[<a
href="#N2">2</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Essa, Ananda, é a exposição do Dhamma, um espelho do Dhamma, através do 
qual um nobre discípulo, se ele assim desejar, poderá por si próprio
declarar de si mesmo: ‘Eu sou um daqueles que deu fim ao inferno, fim ao reino
animal, fim ao reino dos fantasmas, fim aos planos de miséria, fim aos destinos
ruins, fim aos mundos inferiores. Eu entrei na correnteza, não mais destinado
aos mundos inferiores, com o destino fixo, tenho a iluminação como destino.’”<o:p></o:p></span></p>


