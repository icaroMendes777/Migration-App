<p class=Normal> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: "Bhikkhus, este é um modo de vida inferior, esmolar comida. Neste mundo, bhikkhus, é um insulto dizer 'Você, esmolador de comida! Perambulando com uma tigela na mão!' No entanto este meio de vida foi adotado por jovens de boa família por uma razão, com um propósito. Eles não foram forçados por reis, nem por ladrões, nem por dívidas, nem por medo, nem pela perda de um outro meio de vida, mas com o pensamento: 'Eu sou uma vítima do nascimento, envelhecimento e morte, da tristeza, lamentação, dor, angústia e desespero; eu sou uma vítima do sofrimento, uma presa do sofrimento. Com certeza, um fim a toda essa massa de sofrimento pode ser apreendido.'</p>

<p class=Normal>"Assim esse jovem de boa família deixa a vida em família pela vida santa, mas ele pode ser cobiçoso pelos prazeres sensuais, com intensas paixões, com má vontade na mente, com pensamentos corrompidos, sem atenção plena, sem plena consciência, desconcentrado,  com a mente
dispersa e as faculdades descontroladas. Tal como um pedaço de lenha de uma pira funerária, ardendo em ambas pontas e suja de excremento no meio, não pode ser usado como madeira no vilarejo ou na floresta, assim através desse símile eu digo acerca dessa pessoa: ela perdeu os prazeres da vida em família e não realiza o propósito da vida santa."</p>

<p class=Normal style='margin-left:.75in'> Ele perdeu ambos o prazer dos leigos<br>
e da vida santa também, esse homem desafortunado! <br>
Arruinado, ele joga tudo fora<br>
e morre como um tronco numa pira funerária.</p>

<p class=Normal style='margin-left:.75in'> Melhor que ele engula<br> 
uma bola de ferro incandescente<br>
do que esse imoral descontrolado<br>
se alimentar de comida esmolada.</p>


