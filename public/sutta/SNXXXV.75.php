<p class=Normal> Em Savatthi. Então, um certo bhikkhu foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse: “Venerável senhor, em tal e qual habitação, há um certo monge recém ordenado, não muito conhecido, que está enfermo, aflito, gravemente doente. Seria bom, venerável senhor, se o Abençoado pudesse visitar esse bhikkhu por compaixão.” </p>

<p class=Normal>Então, quando o Abençoado ouviu as palavras “recém ordenado” e “enfermo”, e compreendeu que se tratava de um bhikkhu não muito conhecido, ele foi visitá-lo. Aquele bhikkhu viu o Abençoado vindo à distância e ficou agitado na cama. O Abençoado disse: “Já basta, bhikkhu, não fique agitado na cama. Há assentos preparados, eu sentarei aqui.” </p>

<p class=Normal>O Abençoado então, sentou no assento que estava preparado e disse para aquele bhikkhu: “Eu espero que você esteja melhorando, bhikkhu, espero que você esteja confortável, espero que as suas sensações de dor estejam diminuindo e não aumentando e que a sua diminuição, não o seu aumento, seja evidente.” </p>

<p class=Normal> “Venerável Senhor, eu não estou melhorando, não me sinto confortável. Minhas sensações de dor estão aumentando, não diminuindo, o seu aumento, não a sua diminuição é evidente.” </p>

<p class=Normal> “Eu espero então, bhikkhu, que você não esteja atormentado pelo remorso e arrependimento.” </p>

<p class=Normal> “De fato, venerável senhor, eu sinto muito remorso e arrependimento.” </p>

<p class=Normal> “Eu espero, bhikkhu, que você não tenha nenhuma razão para se censurar com relação à virtude.” </p>

<p class=Normal> “Eu não tenho nenhuma razão, venerável senhor, para censurar-me com relação à virtude.” </p>

<p class=Normal> “Então, bhikkhu, se você não tem nenhuma razão para se censurar com relação à virtude, porque você está atormentado pelo remorso e arrependimento?” </p>

<p class=Normal> “Eu entendo, venerável senhor, que não é com o propósito de purificar a virtude que o Dhamma tem sido ensinado pelo Abençoado.” </p>

<p class=Normal> “Se, bhikkhu, você entende que o Dhamma não tem sido ensinado por mim com o propósito de purificar a virtude, então por qual propósito você entende ter sido o Dhamma ensinado por mim?” </p>

<p class=Normal> “Venerável senhor, eu entendo que o Dhamma tem sido ensinado pelo Abençoado com o propósito de nibbana final sem apego.” </p>

<p class=Normal> “Muito bem, bhikkhu! É bom que você entenda que o Dhamma tenha sido ensinado por mim com o propósito de nibbana final sem apego. Pois o Dhamma é ensinado por mim com o propósito de nibbana final sem apego. </p>

<p class=Normal> “O que você pensa, bhikkhu, o olho é permanente ou impermanente?”- “Impermanente, venerável senhor.” ... “O ouvido ... a mente é permanente ou impermanente?” – “Impermanente, venerável senhor.” – “Aquilo que é impermanente é sofrimento ou felicidade?” – “Sofrimento, venerável senhor.” – “Aquilo que é impermanente, sofrimento e sujeito à mudança é adequado que se considere assim: ‘Isso é meu, isso sou eu, isso é o meu eu’?” – Não, venerável senhor.” </p>

<p class=Normal> “Vendo dessa forma, o nobre discípulo bem instruído se desencanta com o olho ... desencanta com a mente. Desencantado ele se torna desapegado. Através do desapego a sua mente é libertada. Quando ela está libertada surge o conhecimento: ‘Libertada.’ Ele compreende que: ‘O nascimento foi destruído, a vida santa foi vivida, o que deveria ser feito foi feito, não há mais vir a ser a nenhum estado.’” </p>

<p class=Normal>Isso foi o que o Abençoado disse. O bhikkhu ficou satisfeito e contente com as palavras do Abençoado. E enquanto essa explanação estava sendo dada a mente daquele bhikkhu foi libertada das impurezas através do desapego." </p>

