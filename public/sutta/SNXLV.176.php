<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, há esses cinco elementos do prazer sensual. <a name=R1></a>Quais
cinco? Formas conscientizadas através do olho que são desejáveis, agradáveis e
fáceis de serem gostadas, conectadas com o desejo sensual e que provocam a
cobiça. Sons conscientizados através do ouvido … Aromas conscientizados através do nariz
… Sabores conscientizados através da língua …Tangíveis conscientizados através do corpo que
são desejáveis, agradáveis e fáceis de serem gostados, conectados com o desejo
sensual e que provocam a cobiça. Esses são os cinco elementos do prazer
sensual. O Nobre Caminho Óctuplo deve ser desenvolvido para o conhecimento
direto desses cinco elementos do prazer sensual, para a sua completa
compreensão, para a sua total destruição e para o seu abandono.” <o:p></o:p></span></p>

