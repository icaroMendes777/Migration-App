<p class=Normal> “Se enquanto estiver caminhando, estiver em pé, estiver sentado, estiver deitado acordado, surgir num bhikkhu um pensamento de desejo sensual, um pensamento de má vontade, um pensamento de crueldade, e ele o tolera, não o abandona, não o dispersa, não o elimina e abole, esse bhikkhu - que desse modo, de tempos em tempos, está carente de ardência e da vergonha de cometer transgressões [<a href="#N1">1</a>] <a name="R1"> </a> -  é chamado indolente e destituído de energia. </p>

<p class=Normal> “Se enquanto estiver caminhando, estiver em pé, estiver sentado, estiver deitado acordado, surgir num bhikkhu um pensamento de desejo sensual, um pensamento de má vontade, um pensamento de crueldade, e ele não o tolera, mas o abandona, o dispersa, o elimina e abole, esse bhikkhu - que desse modo, de tempos em tempos, demonstra ardência e  vergonha de cometer transgressões -  é chamado energético e determinado. </p>

<p class=Normal style='margin-left:.75in'>Quer seja caminhando ou em pé,
quer seja sentado ou deitado,<br>
aquele que nutre pensamentos prejudiciais<br>
relacionados com a vida no mundo<br>
está caminhando por um caminho perigoso, <br>
seduzido por coisas que enganam. <br>
Esse bhikkhu será incapaz de realizar<br>
a iluminação, o objetivo supremo. </p>

<p class=Normal style='margin-left:.75in'>Quer seja caminhando ou em pé,
quer seja sentado ou deitado, <br>
aquele que subjuga esses pensamentos<br>
encontra o prazer no silenciar da mente. <br>
Esse bhikkhu será bem capaz de realizar<br>
a iluminação, o objetivo supremo. </p>

