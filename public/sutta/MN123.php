<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no
Parque de Anathapindika.<o:p></o:p></span></p>



<p class=Normal><a name=T2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2. Agora, um grande número de bhikkhus estavam sentados no salão de
assembléias, onde eles haviam se reunido depois de retornar da esmola de
alimentos, após a refeição, quando esta discussão teve início: “É maravilhoso,
amigos, é admirável, quão poderoso e hábil é o Tathagata! Pois ele é capaz de
saber sobre os Budas do passado – que realizaram o Parinibbana, eliminaram [o
emaranhado da] proliferação, cortaram o desejo, deram fim ao ciclo, e superaram
todo o sofrimento – que para aqueles Abençoados, o nascimento deles havia sido
assim, os seus nomes foram esses, os seus clãs foram esses, as suas virtudes
eram assim, o estado deles [de concentração] era assim, a sabedoria deles era
assim, a sua permanência [nas realizações] era assim, a libertação deles foi
assim.” [<a href="#N1">1</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Quando isso foi dito, o venerável Ananda disse para os bhikkhus:
“Amigos, os Tathagatas são maravilhosos e possuem qualidades maravilhosas. Os Tathagatas
são admiráveis e possuem qualidades admiráveis.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>No entanto,
a discussão deles foi interrompida pois o Abençoado se levantou da meditação ao
anoitecer, foi até o salão de assembléias e sentou num assento que havia sido
preparado. Então, ele se dirigiu aos bhikkhus da seguinte forma: “Bhikkhus, qual
é o assunto que faz com que vocês estejam sentados juntos aqui agora? E qual é
a discussão que foi interrompida?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
venerável senhor, estávamos sentados no salão de assembléias, onde nos havíamos
reunido depois de retornar da esmola de alimentos, após a refeição, quando esta
discussão teve início: “É maravilhoso, amigos, é admirável ... a libertação
deles foi assim.’ Quando isso foi dito, venerável senhor, o venerável Ananda
nos disse: ‘Amigos, os Tathagatas são maravilhosos e possuem qualidades
maravilhosas. Os Tathagatas são admiráveis e possuem qualidades admiráveis.’
Essa era a nossa discussão, venerável senhor, que foi interrompida quando o
Abençoado chegou.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o
Abençoado se dirigiu ao venerável Ananda: “Em sendo assim, Ananda, explique de
forma mais completa as qualidades maravilhosas e admiráveis de um Tathagata.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. “Eu ouvi
e aprendi isto, venerável senhor, da própria boca do Abençoado: ‘Plenamente
atento e plenamente consciente, Ananda, o Bodisatva renasceu no paraíso de
Tusita.’ [<a href="#N2">2</a>] <a name=R2></a>Que plenamente atento e
plenamente consciente o Bodisatva renasceu no paraíso de<span
style='mso-spacerun:yes'>  </span>Tusita – isso eu me lembro como uma qualidade
maravilhosa e admirável do Abençoado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. “Eu ouvi
e aprendi isto da própria boca do Abençoado: ‘Plenamente atento e plenamente
consciente, o Bodisatva permaneceu no paraíso de Tusita.’ Isso também eu me
lembro como uma qualidade maravilhosa e admirável do Abençoado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. “Eu ouvi
e aprendi isto da própria boca do Abençoado: ‘Durante toda a duração do seu
tempo de vida o Bodisatva permaneceu no paraíso de Tusita.’<span
style='mso-spacerun:yes'>  </span>Isso também eu me lembro como uma qualidade
maravilhosa e admirável do Abençoado.<o:p></o:p></span></p>



<p class=Normal><a name=T6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>6. “Eu ouvi e aprendi isto da própria boca do Abençoado: ‘Plenamente
atento e plenamente consciente, o Bodisatva faleceu no paraíso de Tusita e
descendeu no ventre da sua mãe.’ Isso também eu me lembro como uma qualidade
maravilhosa e admirável do Abençoado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. “Eu ouvi
e aprendi isto da própria boca do Abençoado: ‘Quando o Bodisatva faleceu<span
style='mso-spacerun:yes'>  </span>no paraíso de Tusita e descendeu no ventre da
sua mãe, então uma grande e imensurável luz superando o esplendor dos devas
apareceu no mundo com as suas divindades, Maras e Brahmas, com os seus
contemplativos e brâmanes, seus príncipes e povo. E mesmo nos intervalos
abismais entre os mundos, vazios, sombrios e completamente escuros, onde a lua
e o sol, apesar de fortes e poderosos, não são capazes de fazer prevalecer a
sua luz – lá também apareceu uma grande e imensurável luminosidade superando o
esplendor dos devas. [<a href="#N3">3</a>] <a name=R3></a>E os seres que ali
nasceram perceberam uns aos outros através daquela luz: “Então outros seres, de
fato, surgiram aqui.” E este imenso universo se agitou, sacudiu e tremeu e lá
também apareceu uma luminosidade grande e imensurável superando o esplendor dos
devas.’<span style='mso-spacerun:yes'>  </span>Isso também eu me lembro como
uma qualidade maravilhosa e admirável do Abençoado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. “Eu ouvi
e aprendi isto da própria boca do Abençoado: ‘Quando o Bodisatva descendeu no
ventre da sua mãe, quatro jovens devas vieram protegê-lo nos quatro cantos de
modo que nenhum humano ou não humano, ou qualquer um, pudessem causar dano ao
Bodisatva ou à mãe dele.’ [<a href="#N4">4</a>] <a name=R4></a>Isso também eu
me lembro como uma qualidade maravilhosa e admirável do Abençoado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. “Eu ouvi
e aprendi isto da própria boca do Abençoado: ‘Quando o Bodisatva descendeu no
ventre da sua mãe, ela se tornou virtuosa de forma intrínseca, abstendo-se de
matar seres vivos, de tomar aquilo que não é dado, da conduta imprópria com
relação aos prazeres sensuais, da linguagem mentirosa e do vinho, álcool e
outros embriagantes, que causam a negligência.’ Isso também eu me lembro como
uma qualidade maravilhosa e admirável do Abençoado. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. “Eu
ouvi e aprendi isto da própria boca do Abençoado: ‘Quando o Bodisatva descendeu
no ventre da sua mãe, nenhum pensamento sensual surgiu nela com relação aos
homens, e ela esteve inacessível a qualquer homem que tivesse uma mente
lasciva.’ Isso também eu me lembro como uma qualidade maravilhosa e admirável
do Abençoado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. “Eu
ouvi e aprendi isto da própria boca do Abençoado: ‘Quando o Bodisatva descendeu
no ventre da sua mãe, ela obteve os cinco elementos do prazer sensual e assim
provida e dotada, ela deles desfrutou.’ Isso também eu me lembro como uma
qualidade maravilhosa e admirável do Abençoado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. “Eu
ouvi e aprendi isto da própria boca do Abençoado: ‘Quando o Bodisatva descendeu
no ventre da sua mãe, nenhum tipo de aflição surgiu nela, ela esteve feliz e
livre do cansaço corporal. Ela viu o Bodisatva no seu ventre com todos os seus
membros, sem estar desprovido de qualquer faculdade. Suponham que um fio azul,
amarelo, vermelho, branco ou marrom fosse passado por uma fina gema de berilo
da mais pura água, com oito facetas, bem lapidada, e um homem com boa visão a
tomasse nas mãos e examinasse assim: “Esta é uma fina gema de berilo da mais
pura água, com oito facetas, bem lapidada e através dela passa esse fio azul,
amarelo, vermelho, branco ou marrom”; da mesma forma quando o Bodisatva
descendeu no ventre da sua mãe ... Ela viu o Bodisatva no seu ventre com todos
os seus membros, sem estar desprovido de qualquer faculdade.’ Isso também eu me
lembro como uma qualidade maravilhosa e admirável do Abençoado. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. “Eu
ouvi e aprendi isto da própria boca do Abençoado: ‘Sete dias após o nascimento
do Bodisatva, a sua mãe morreu e renasceu no paraíso de Tusita.’ [<a href="#N5">5</a>]
<a name=R5></a>Isso também eu me lembro como uma qualidade maravilhosa e
admirável do Abençoado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14. “Eu
ouvi e aprendi isto da própria boca do Abençoado: ‘Outras mulheres dão à luz
depois de carregar o feto no ventre por nove ou dez meses, mas não a mãe do
Bodisatva. A mãe do Bodisatva deu à luz depois de carregá-lo no ventre por
exatos dez meses.’ Isso também eu me lembro como uma qualidade maravilhosa e
admirável do Abençoado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. “Eu
ouvi e aprendi isto da própria boca do Abençoado: ‘Outras mulheres dão à luz
sentadas ou deitadas, mas não a mãe do Bodisatva. A mãe do Bodisatva deu à luz
em pé.’<span style='mso-spacerun:yes'>  </span>Isso também eu me lembro como
uma qualidade maravilhosa e admirável do Abençoado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16. “Eu
ouvi e aprendi isto da própria boca do Abençoado: ‘Quando o Bodisatva saiu do
ventre da sua mãe, primeiro devas o receberam e depois os seres humanos.’ Isso
também eu me lembro como uma qualidade maravilhosa e admirável do Abençoado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17. “Eu
ouvi e aprendi isto da própria boca do Abençoado: ‘Quando o Bodisatva saiu do
ventre da sua mãe, ele não tocou a terra. Os quatro jovens devas o receberam e
o colocaram à frente da sua mãe dizendo: “Alegre-se, rainha, um filho com
grande poder acaba de nascer.’” Isso também eu me lembro como uma qualidade
maravilhosa e admirável do Abençoado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18. “Eu
ouvi e aprendi isto da própria boca do Abençoado: ‘Quando o Bodisatva saiu do
ventre da sua mãe, ele saiu imaculado, sem manchas de água ou humores ou sangue
ou qualquer tipo de impureza, limpo e imaculado. Suponham que houvesse uma gema
colocada sobre um tecido de Kasi, então a gema não iria manchar o tecido ou o
tecido a gema. Por que isso? Devido à pureza de ambos. Da mesma forma quando o
Bodisatva saiu do ventre da sua mãe ... limpo e imaculado.’ Isso também eu me
lembro como uma qualidade maravilhosa e admirável do Abençoado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>19. “Eu
ouvi e aprendi isto da própria boca do Abençoado: ‘Quando o Bodisatva saiu do
ventre da sua mãe, dois jatos de água apareceram jorrando do céu, um frio e o
outro quente, para banhar o Bodisatva e a sua mãe.’ Isso também eu me lembro
como uma qualidade maravilhosa e admirável do Abençoado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>20. “Eu
ouvi e aprendi isto da própria boca do Abençoado: ‘Assim que o Bodisatva saiu
do ventre da sua mãe, ele ficou em pé, firme com os pés sobre a terra; então
ele caminhou sete passos para o norte, e com um pára-sol branco mantido sobre
si, ele inspecionou cada quadrante e pronunciou as palavras do Líder do
Rebanho: “Eu sou o superior no mundo; Eu sou o melhor no mundo;<span
style='mso-spacerun:yes'>  </span>Eu sou o primeiro no mundo. Este é o meu
último nascimento; agora não haverá outro devir para mim.’” [<a href="#N6">6</a>]
<a name=R6></a>Isso também eu me lembro como uma qualidade maravilhosa e
admirável do Abençoado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>21. “Eu
ouvi e aprendi isto da própria boca do Abençoado: ‘Quando o Bodisatva saiu do
ventre da sua mãe, então uma grande e imensurável luminosidade superando o
esplendor dos devas apareceu no mundo com as suas divindades, Maras e Brahmas,
com os seus contemplativos e brâmanes, seus príncipes e<span
style='mso-spacerun:yes'>  </span>povo. E mesmo nos intervalos abismais entre
os mundos, vazios, sombrios e completamente escuros, onde a lua e o sol, apesar
de fortes e poderosos, não são capazes de fazer prevalecer a sua luz – lá
também apareceu uma grande e imensurável luminosidade superando o esplendor dos
devas. E os seres que ali nasceram perceberam uns aos outros através daquela
luz: “Então outros seres, de fato, surgiram aqui.” E este imenso universo se
agitou, sacudiu e tremeu e lá também apareceu uma luminosidade grande e
imensurável superando o esplendor dos devas.’ Que quando o Bodisatva saiu do
ventre da sua mãe, então uma grande e imensurável luminosidade superando o esplendor
dos devas apareceu ... isso também eu me lembro como uma qualidade maravilhosa
e admirável do Abençoado.”<o:p></o:p></span></p>



<p class=Normal><a name=T22></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>22. “Em sendo assim, Ananda, lembre-se disto também como uma qualidade
maravilhosa e admirável do Tathagata: Aqui, Ananda, para o Tathagata as sensações
são conhecidas quando elas surgem, enquanto estão presentes, quando
desaparecem; as percepções são conhecidas quando elas surgem, enquanto estão
presentes, quando desaparecem; os objetos mentais são conhecidos quando eles
surgem, enquanto estão presentes, quando desaparecem. [<a href="#N7">7</a>] <a
name=R7></a>Lembre-se disso também, Ananda, como uma qualidade maravilhosa e
admirável do Tathagata.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>23.
“Venerável senhor, visto que para o Abençoado as sensações são conhecidas
quando elas surgem, enquanto estão presentes, quando desaparecem; as percepções
são conhecidas quando elas surgem, enquanto estão presentes, quando
desaparecem; os pensamentos são conhecidos quando eles surgem, enquanto estão
presentes, quando desaparecem – isso também eu me lembrarei como uma qualidade
maravilhosa e admirável do Tathagata.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o
que disse o venerável Ananda. O Mestre aprovou. Os bhikkhus ficaram satisfeitos
e contentes com as palavras do venerável Ananda.<o:p></o:p></span></p>

