<p class=Normal> Certa ocasião o Abençoado estava em Rajagaha no Manguezal de Jivaka. Lá ele se dirigiu aos bhikkhus:</p>

<p class=Normal> “Bhikkhus, desenvolvam a concentração. Quando um bhikkhu está concentrado as coisas são compreendidas como na verdade são. O que é compreendido como na verdade é? O olho é compreendido como na verdade é - como impermanente. As formas são compreendidas como na verdade são - como impermanentes. A consciência no olho ... o contato no olho ... qualquer sensação que surja tendo o contato no olho como condição - quer seja prazerosa, dolorosa ou nem prazerosa nem dolorosa - é compreendida como na verdade é - como impermanente.</p>

<p class=Normal> "O ouvido é compreendido como na verdade é ... a mente é compreendida como na verdade é ... qualquer sensação que surja tendo o contato na mente como condição - quer seja prazerosa, dolorosa ou nem prazerosa nem dolorosa - é compreendida como na verdade é - como impermanente.</p>

<p class=Normal> "Desenvolvam a concentração, bhikkhus, quando um bhikkhu está concentrado as coisas são compreendidas como na verdade são."</p>

