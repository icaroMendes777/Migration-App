<p class=Normal> Em Savatthi. “Bhikkhus, aquilo que alguém intenciona, aquilo que alguém planeja e aquilo
que alguém tenha como tendência subjacente: isto se torna uma base para a manutenção da consciência. Quando há uma base, haverá um suporte para o estabelecimento da consciência. Quando a consciência se estabelece e cresce, a mentalidade-materialidade (nome e forma) é estabelecida. Com a mentalidade-materialidade (nome e forma) como condição, as seis bases dos sentidos surgem; com as seis bases dos sentidos como condição, o contato surge; com o contato como condição, a sensação ... desejo ... apego ... ser/existir ... nascimento; com o nascimento como condição, o envelhecimento e morte, lamentação, dor, angústia e desespero surgem. Essa é a origem de toda essa massa de sofrimento.</p>

<p class=Normal> “Se, bhikkhus, alguém não intenciona, alguém não planeja, mas ainda tem como tendência subjacente, isto se torna uma base para a manutenção da consciência. Quando há uma base, haverá um suporte para o estabelecimento da consciência ... Essa é a origem de toda essa massa de sofrimento.</p>

<p class=Normal> “Mas, bhikkhus, quando alguém não intenciona, alguém não planeja e não tem como tendência subjacente, nenhuma base existe para a manutenção da consciência. Quando não há uma base, não haverá suporte para o estabelecimento da consciência. Quando a consciência não se estabelece e não cresce, não há o estabelecimento da mentalidade-materialidade (nome e forma). Com a cessação da mentalidade-materialidade (nome e forma) ocorre a cessação das seis bases dos sentidos ... Essa é a cessação de toda essa massa de sofrimento."</p>

