<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] A faculdade ‘eu compreenderei aquilo que ainda não é compreendido’ é
a faculdade que surge no momento da realização do caminho do entrar na
correnteza naquele que pratica com o pensamento, ‘eu compreenderei o Dhamma que
não compreendi antes no samsara’ A faculdade do conhecimento supremo é a faculdade
que surge nas seis ocasiões a partir da realização do fruto do entrar na
correnteza até a realização do caminho do arahant; ela ocorre numa condição de
compreensão em maior profundidade daquelas mesmas coisas compreendidas pela
realização do primeiro caminho (entrar na correnteza). A faculdade daquele
dotado com o conhecimento supremo é a faculdade que surge em relação àquelas
coisas completamente compreendidas através da realização do fruto do arahant. [<a
href="#R1">Retorna</a>]<o:p></o:p></span></p>




