<p class=Normal><a name=R1></a> Certa ocasião o Abençoado estava em Savathi, no Bosque de Jeta, no Parque de Anathapindika. Então a Princesa Sumana, [<a href="#N1">1</a>] acompanhada por quinhentas carruagens e quinhentas acompanhantes da corte foi até o Abençoado e depois de cumprimentá-lo ela sentou a um lado e disse para o Abençoado: </p>

<p class=Normal> "Aqui, venerável senhor, podem haver dois discípulos do Abençoado iguais em convicção, comportamento virtuoso e sabedoria, mas um deles é generoso enquanto que o outro não é. Com a dissolução do corpo, após a morte. ambos renasceriam num destino feliz, no paraíso. Quando eles se tornassem devas, haveria alguma distinção ou diferença entre os dois?" </p>

<p class=Normal> "Haveria Sumana," o Abençoado respondeu. "O generoso, tendo se tornado um deva, superaria o outro em cinco aspectos: no tempo de vida celestial, na beleza celestial, na felicidade celestial, na glória celestial e na autoridade celestial. O generoso, tendo se tornado um deva, superaria o outro nesses cinco aspectos." </p>

<p class=Normal> "Mas, venerável senhor, se os dois falecessem do paraíso e novamente se tornassem seres humanos, ainda haveria alguma distinção ou diferença entre os dois?" </p>

<p class=Normal> "Haveria Sumana," o Abençoado respondeu. "Quando eles novamente se tornassem seres humanos, o generoso superaria o outro em cinco aspectos: no tempo de vida humano, na beleza humana, na felicidade humana, na glória humana e na autoridade humana. Quando eles novamente se tornassem seres humanos, o generoso superaria o outro nesses cinco aspectos." </p>

<p class=Normal> "Mas, venerável senhor, se os dois abandonassem a vida em família e seguissem a vida santa, ainda haveria alguma distinção ou diferença entre os dois?" </p>

<p class=Normal><a name=R2></a> "Haveria Sumana," o Abençoado respondeu. "O generoso tendo seguido a vida santa superaria o outro em cinco aspectos: (1) Em geral ele usaria mantos que tivessem sido especificamente oferecidos para ele, raramente ele usaria mantos que não tivessem sido especificamente oferecidos para ele. [<a href="#N2">2</a>]  (2) Em geral ele comeria comida esmolada que tivesse sido especificamente oferecida para ele, raramente ele comeria comida esmolada que não tivesse sido especificamente oferecida para ele. (3) Em geral ele usaria moradia que tivesse sido especificamente oferecida para ele, raramente ele usaria moradia que não tivesse sido especificamente oferecida para ele. (4) Em geral ele usaria medicamentos que tivessem sido especificamente oferecidos para ele, raramente ele usaria medicamentos que não tivessem sido especificamente oferecidos para ele. (5) Os seus companheiros monásticos, aqueles com quem ele convive, em geral se comportariam de modo agradável através do corpo, linguagem e mente, raramente de modo desagradável. O generoso tendo seguido a vida santa superaria o outro nesses cinco aspectos." </p>

<p class=Normal> "Mas, venerável senhor, se os dois realizassem o estado de arahant, ainda haveria alguma distinção ou diferença entre os dois?" </p>

<p class=Normal> "Nesse caso, Sumana, eu declaro que não haveria diferença entre a libertação de um e a libertação do outro." </p>

<p class=Normal>"É maravilhoso e admirável, venerável senhor! Deveras, todos têm bons motivos para prover alimentos e realizar ações meritórias, visto que isso será benéfico se alguém se tornar um deva, um ser humano, ou seguir a vida santa." </p>

<p class=Normal> "Assim é, Sumana! Assim é, Sumana! Deveras, todos têm bons motivos para prover alimentos e realizar ações meritórias, visto que isso será benéfico se alguém se tornar um deva, um ser humano, ou seguir a vida santa." </p>

<p class=Normal> Isso foi o que o Abençoado disse. Tendo dito isso, o Iluminado, o Mestre, disse ainda mais: </p>

<p class=Normal> "Tal como a lua imaculada <br>
movendo-se através do espaço<br>
supera com o seu brilho<br>
todas as estrelas no mundo, <br>
assim também quem é consumado no comportamento virtuoso, <br>
uma pessoa dotada de convicção, <br>
supera com a generosidade<br>
todos os avaros no mundo. </p>

<p class=Normal> "Tal como uma imensa nuvem de chuva, <br>
trovejando, coroada por raios, <br>
derrama a chuva sobre a terra, <br>
inundando as planícies e as baixadas, <br>
assim também o discípulo do Iluminado, <br>
sábio com a visão consumada, <br>
supera o avaro<br>
em cinco aspectos: <br>
tempo de vida e glória, <br>
beleza e felicidade. <br>
Possuindo riqueza, após a morte<br>
ele se delicia no paraíso." </p>

