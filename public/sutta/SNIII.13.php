<p class=Normal><a name=N23></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Em Savatthi. Agora, naquela ocasião o Rei Pasenadi de Kosala comeu o
equivalente a um balde de comida e depois foi até o Abençoado, ofegante e cheio
de tanto comer, e depois de cumprimentá-lo sentou a um lado. O Abençoado
percebendo que o Rei Pasenadi estava ofegante e cheio de tanto comer,
aproveitou a ocasião para pronunciar estes versos:<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><i><span
lang=PT-BR style='mso-ansi-language:PT-BR'>manujassa sada satimato</span></i> <br>&nbsp;&nbsp; Quando uma pessoa tem constante
atenção plena,<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><i> <span lang=PT-BR
style='mso-ansi-language:PT-BR'>mattam janato laddha-bhojane</span></i> <br>&nbsp;&nbsp;e
sabe quando ingeriu comida suficiente,<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><i><span lang=PT-BR
style='mso-ansi-language:PT-BR'>tanu tassa bhavanti vedana</span></i> <br>&nbsp;&nbsp;todas
as suas aflições emagrecem<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><i><span
lang=PT-BR style='mso-ansi-language:PT-BR'>sanikam jirati ayu palayan-ti</span></i> <br>&nbsp;&nbsp;ela
envelhece mais gradualmente, protegendo a sua vida.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Agora,
naquela ocasião o jovem brâmane Sudassana estava em pé atrás do Rei Pasenadi de
Kosala. O rei então se dirigiu a ele da seguinte forma: “Venha agora, estimado
Sudassana, aprenda esses versos do Abençoado e recite-os sempre que eu estiver
tomando as minhas refeições. E eu irei estabelecer para você um pagamento
permanente de cem kahopanas a cada dia.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
majestade” o jovem brâmane Sudassana respondeu ao rei.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim o Rei
Pasenadi de Kosala gradualmente reduziu a quantidade de comida ingerida até que
no máximo passou a comer não mais do que um pint de arroz cozido. Mais tarde,
quando o seu corpo havia se tornado bastante delgado, o Rei Pasenadi acariciou
os seus membros com a mão e aproveitou a ocasião para pronunciar estes versos:<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><i><span lang=PT-BR
style='mso-ansi-language:PT-BR'>ubhayena vata mam so</span></i> <br>&nbsp;&nbsp;De
fato, o Buda me mostrou<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><i><span lang=PT-BR
style='mso-ansi-language:PT-BR'>Bhagava atthena anukampi</span></i> <br>&nbsp;&nbsp;compaixão
de duas formas distintas: <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><i><span lang=PT-BR
style='mso-ansi-language:PT-BR'>ditthadhammikena c-eva</span></i> <br>&nbsp;&nbsp;para
o meu bem estar aqui e agora,<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><i><span lang=PT-BR
style='mso-ansi-language:PT-BR'>samparayikena ca ti.</span></i> <br>&nbsp;&nbsp;e
também no futuro.<o:p></o:p></span></p>


<p class=Normal>&nbsp;</p>

<hr size=2 width="100%" align=center>

<p class=Normal>&nbsp;</p>


<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Nota:<o:p></o:p></span></b></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quem
poderia imaginar que perder peso fosse tão fácil! Nesta breve conversa, o Buda
sugere que comer em excesso é a causa da obesidade, que acelera o processo de
envelhecimento e coloca em risco a própria vida e que isso só ocorre quando a
atenção plena é fraca ou está ausente. Se comermos lentamente e com grande
atenção, ficará mais facilmente aparente, (se formos honestos conosco), quando
uma quantidade adequada de comida tiver sido consumida. É interessante notar
que o Buda parece indicar que a sabedoria proporcionará o que for necessário
para nos abstermos de comer mais, ao invés da idéia moderna convencional de que
isso requer força de vontade ou autocontrole.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Fazendo uso
de um jogo de palavras, o Buda diz que todas as nossas aflições, (literalmente,
todas as nossas sensações desagradáveis), e não apenas os nossos corpos, irão
“emagrecer.” Talvez o Rei Pasenadi esteja se referindo a isso ao dizer que o
ensinamento do Buda não somente o auxiliou a emagrecer o corpo, (o benefício
imediato), mas que o incremento geral da atenção plena e a diminuição da cobiça
irão auxiliar em todos aspectos da vida espiritual (e conseqüentemente no seu
renascimento no futuro).<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O
comentário deste sutta nos informa que o rei não empregou Sudassana para
recitar o verso durante toda a refeição, mas apenas quando ele começasse a
comer. A idéia não é cultivar a aversão pela comida, pois a comida em si não é
ruim. Como ocorre com muitos outros ensinamentos Budistas, é uma questão de
entender o processo de causa e efeito e de usar a comida com habilidade como
uma ferramenta para o despertar, ao invés de permitir que sejamos capturados
pelas obsessões do apego, aversão e confusão que podem ser despertadas pelo
nosso relacionamento com a comida.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Observe a
linguagem da última linha do verso do Buda. A palavra para vida (<i>ayu</i>) é
a mesma da tradição médica Hindu – Ayurveda, (= conhecimento da vida), e é
considerado como algo que pode ser desperdiçado ou guardado com cuidado. Quando
guardado com cuidado a preservação da vida também diminui o processo de
envelhecimento. A imagem não é de alguém conquistando a enfermidade ou a morte,
(pois isto só ocorre com o completo despertar), mas de tratar o recurso
precioso da própria vitalidade com sabedoria.<o:p></o:p></span></p>



