<p class=Tit3 align=center style='text-align:center'><b>Samyutta Nikaya III.13</b></Tit3>
<p class=Tit1 align=center style='text-align:center'>Donapaka Sutta </Tit1>
<p class=Tit1 align=center style='text-align:center'>O Rei Pasenadi faz Dieta </Tit1>

