<p class=Normal> "Bhikkhus, há esses cinco grilhões na mente. Quais cinco? </p>

<p class=Normal> (1) “Aqui, um bhikkhu não está livre da paixão, desejo, afeição, sede, cobiça e ambição pelos prazeres sensuais, e dessa forma a mente dele não se inclina pelo ardor, devoção, perseverança e esforço. Como a mente dele não se inclina pelo ardor, devoção, perseverança e esforço, esse é o primeiro grilhão na mente que ele não partiu. </p>

<p class=Normal><a name=R1></a> (2) “Outra vez, um bhikkhu não está livre da paixão, desejo, afeição, sede, cobiça e ambição pelo corpo [<a href="#N1">1</a>]   ... Como a mente dele não se inclina pelo ardor ... esse é o segundo grilhão na mente que ele não partiu. </p>

<p class=Normal> (3) “Outra vez, um bhikkhu não está livre da paixão, desejo, afeição, sede, cobiça e ambição pela forma ... Como a mente dele não se inclina pelo ardor ... esse é o terceiro grilhão na mente que ele não partiu. </p>

<p class=Normal> (4) “Outra vez, um bhikkhu come tanto quanto ele quiser, até que a sua barriga esteja cheia e se entrega aos prazeres da indolência, cochilo e sono ... Como a mente dele não se inclina pelo ardor ... esse é o quarto grilhão na mente que ele não partiu. </p>

<p class=Normal> (5) “Outra vez, um bhikkhu vive a vida santa aspirando por algum plano divino assim: ‘Por esta virtude, ou observância, ou ascetismo, ou vida santa, eu me tornarei um [grande] deva ou algum deva [menor], e desse modo a mente dele não se inclina pelo ardor, devoção, perseverança e esforço. Como a mente dele não se inclina pelo ardor, devoção, perseverança e esforço, esse é o quinto grilhão na mente que ele não partiu. </p>

<p class=Normal>"Esses, bhikkhus, são os cinco grilhões na mente." </p>

