<p class=Normal> Certa ocasião, o Ven. Sariputta e o Ven. Maha Kotthita estavam em
Benares, no Parque do Gamo, em
Isipatana. Então, ao anoitecer o Ven. Maha Kotthita saiu do seu isolamento e foi
até o Ven. Sariputta e  ambos
se cumprimentaram. Quando a conversa cortês e amigável havia terminado ele sentou a um
lado e disse para o Ven. Sariputta: <o:p></o:p></span></p>


<p class=Normal> “Como é, amigo Sariputta, o olho é o grilhão das formas ou são as
formas o grilhão do olho? O ouvido ... O nariz ... A língua ... O corpo ... A
mente é o grilhão dos objetos mentais ou são os objetos mentais o grilhão da
mente?” <o:p></o:p></span></p>


<p class=Normal> “Amigo Kotthita, o olho não é o grilhão das formas nem são as formas o
grilhão do olho, mas melhor dito, o desejo e a cobiça que surge na dependência
de ambos: esse é o grilhão. O ouvido não é o grilhão dos sons ... O nariz não é
o grilhão dos aromas ... A língua não é o grilhão dos sabores ... O corpo não é
o grilhão das sensações tangíveis ... A mente não é o grilhão dos objetos
mentais, nem os objetos mentais são o grilhão da mente, mas melhor dito, o
desejo e a cobiça que surge na dependência de ambos: esse é o grilhão.<span
style="mso-spacerun: yes">  </span><o:p></o:p></span></p>


<p class=Normal> “Suponha. amigo, que um boi preto e um boi branco fossem unidos por um
único arreio ou canga. Alguém falaria corretamente se dissesse: ‘O boi preto é
o grilhão do boi branco, o boi branco é o grilhão do boi preto’?” <o:p></o:p></span></p>


<p class=Normal> “Não, amigo. O boi preto não é o grilhão do boi branco, nem o boi branco
é o grilhão do preto, mas melhor dito, o arreio ou canga pelo qual eles estão
unidos: esse é o grilhão.&quot; <o:p></o:p></span></p>


<p class=Normal> “Da mesma forma, amigo, o olho não é o grilhão das formas, nem são as
formas o grilhão do olho, mas melhor dito, o desejo e a cobiça que surge na
dependência de ambos: esse é o grilhão. O ouvido não é o grilhão dos sons ... O
nariz não é o grilhão dos aromas ... A língua não é o grilhão dos sabores ... O
corpo não é o grilhão das sensações tangíveis ... A mente não é o grilhão dos
objetos mentais, nem os objetos mentais são o grilhão da mente, mas melhor
dito, o desejo e a cobiça que surge na dependência de ambos: esse é o
grilhão.<span style="mso-spacerun: yes">  </span><o:p></o:p></span></p>


<p class=Normal> “Se, amigo, o olho fosse o grilhão das formas ou se as formas fossem o
grilhão do olho, então esta vida santa para o correto fim do sofrimento não
poderia ser discernida. Mas visto que o olho não é o grilhão das formas, nem as
formas são o grilhão do olho,<span style="mso-spacerun: yes">  </span>mas
melhor dito, o desejo e a cobiça que surge na dependência de ambos é o grilhão,
então a vida santa para o correto fim do sofrimento é discernida. <o:p></o:p></span></p>


<p class=Normal> &quot;Se, amigo, o ouvido fosse o grilhão dos sons ou se os sons fossem
o grilhão do ouvido ... Se a mente fosse o grilhão dos objetos mentais ou se os
objetos mentais fossem o grilhão da mente, então esta vida santa para o correto
fim do sofrimento não poderia ser discernida. Mas visto que a mente não é o
grilhão dos objetos mentais, nem os objetos mentais são o grilhão da
mente,<span style="mso-spacerun: yes">  </span>mas melhor dito, o desejo e a
cobiça que surge na dependência de ambos é o grilhão, então a vida santa para o
correto fim do sofrimento é discernida.. <o:p></o:p></span></p>


<p class=Normal> “Desse modo também, amigo, pode ser compreendido como isto assim é: há
no Abençoado o olho, o Abençoado vê a forma com o olho, no entanto não há
desejo e cobiça na mente do Abençoado; o Abençoado está com a mente bem
libertada. Há no Abençoado o ouvido, o Abençoado ouve um som com o ouvido ...
Há no Abençoado a mente, o Abençoado percebe um objeto mental com a mente, no
entanto não há desejo e cobiça na mente do Abençoado; o Abençoado está com a
mente bem libertada.<span style="mso-spacerun: yes">  </span><o:p></o:p></span></p>


<p class=Normal> “É desse modo, amigo, que pode ser compreendido como o olho não é o
grilhão das formas, nem as formas são o grilhão do olho, mas melhor dito, o
desejo e a cobiça que surge na dependência de ambos: esse é o grilhão. O ouvido
não é o grilhão dos sons ... A mente não é o grilhão dos objetos mentais, nem
os objetos mentais são o grilhão da mente, mas melhor dito, o desejo e a cobiça
que surge na dependência de ambos: esse é o grilhão.” <o:p></o:p></span></p>


