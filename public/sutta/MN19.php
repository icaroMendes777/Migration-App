<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no
Parque de Anathapindika. Lá ele se dirigiu aos monges desta forma: “Bhikkhus”.
– “Venerável Senhor”, eles responderam. O Abençoado disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><a name=T2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2. “Bhikkhus, antes da minha iluminação, quando eu ainda era apenas um
Bodisatva não iluminado, eu pensei: ‘E se eu dividisse os meus pensamentos em
duas categorias.’ [</span><a href="#N1"><span lang=PT-BR style='mso-ansi-language:
PT-BR'>1</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>] <a
name=R1></a>Então coloquei de um lado os pensamentos de desejo sensual,
pensamentos de má vontade e pensamentos de crueldade; e coloquei do outro lado
os pensamentos de renúncia, pensamentos de não má vontade e pensamentos de não
crueldade. [</span><a href="#N2"><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>3. “Enquanto assim permanecia, diligente, ardente e decidido, um
pensamento de desejo sensual surgiu em mim. Eu compreendi desta forma: ‘Este
pensamento de desejo sensual surgiu em mim. Isso conduz à minha própria
aflição, à aflição dos outros e à aflição de ambos; isso obstrui a sabedoria,
causa dificuldades e afasta de Nibbana.’ Ao pensar: ‘Isto conduz à minha própria
aflição,’ aquilo arrefeceu em mim; ao pensar: ‘Isto conduz à aflição dos
outros,’ aquilo arrefeceu em mim; ao pensar: ‘Isto conduz à aflição de ambos,’
aquilo arrefeceu em mim; ao pensar: ‘Isso obstrui a sabedoria, causa dificuldades e
afasta de Nibbana,’ aquilo arrefeceu em mim. Sempre que um desejo sensual
surgia em mim, eu o abandonava, o removia, o eliminava.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4-5
“Enquanto assim permanecia, diligente, ardente e decidido, um pensamento de má
vontade surgiu em mim … um pensamento de crueldade surgiu em mim. Eu compreendi
desta forma: ‘Este pensamento de crueldade surgiu em mim. Isso conduz à minha
própria aflição, à aflição dos outros e à aflição de ambos; isso obstrui a
sabedoria, causa dificuldades e afasta de Nibbana.’ Ao pensar: ‘Isto conduz à
minha própria aflição,’ aquilo arrefeceu em mim; ao pensar: ‘Isto conduz à
aflição dos outros,’ aquilo arrefeceu em mim; ao pensar: ‘Isto conduz à aflição
de ambos,’ aquilo arrefeceu em mim; ao pensar: ‘Isso obstrui a sabedoria, causa
dificuldades e afasta de Nibbana,’ aquilo arrefeceu em mim. Sempre que um
pensamento de crueldade surgia em mim, eu o abandonava, o removia, o eliminava.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6.
“Bhikkhus, qualquer coisa que um bhikkhu pense e pondere com freqüência, essa
passará a ser a tendência da sua mente. Se ele pensar e ponderar com freqüência
pensamentos de desejo sensual, ele terá abandonado o pensamento da renúncia
para cultivar o pensamento do desejo sensual e então a sua mente irá tender
para os pensamentos de desejo sensual. Se ele pensar e ponderar com freqüência
pensamentos de má vontade … pensamentos de crueldade, ele terá abandonado o
pensamento da não crueldade para cultivar o pensamento da crueldade e então a
sua mente irá tender para os pensamentos de crueldade.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. “ Da
mesma maneira como no último mês da estação chuvosa, no outono, quando as
plantações amadurecem, um vaqueiro será cuidadoso com os seus bois, empurrando
e cutucando a lateral do corpo deles com uma vara para mantê-los sob controle e
refreados. Por que isso? Porque ele sabe que poderia ser criticado, multado, açoitado ou
aprisionado, se ele permitisse que eles andassem desgarrados pelas
plantações. Assim também eu vi nos estados prejudiciais o perigo, a degradação
e a contaminação e nos estados benéficos as vantagens da renúncia, a purificação
das contaminações.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8.
“Enquanto permanecia assim, diligente, ardente e decidido, um pensamento de
renúncia surgiu em mim. Eu compreendi desta forma: ‘Este pensamento de renúncia
surgiu em mim. Isso não conduz à minha própria aflição, ou à aflição dos
outros, ou à aflição de ambos; isso auxilia a sabedoria, não causa dificuldades
e conduz a Nibbana. Se eu pensar e ponderar esse pensamento mesmo que seja por
uma noite, mesmo por um dia, mesmo por uma noite e dia, eu não vejo nada que
temer. Mas com o excessivo pensar e ponderar eu poderei cansar meu corpo e
quando o corpo fica cansado, a mente fica tensa e quando a mente fica tensa ela
fica muito distante da concentração.’ Assim,<span style='mso-spacerun:yes'> 
</span>estabilizei a minha mente internamente, tranqüilizei-a, eu a unifiquei e concentrei. Por que isso? Para que a minha mente não
ficasse exausta.[</span><a href="#N3"><span lang=PT-BR style='mso-ansi-language:
PT-BR'>3</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=R3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>9-10. “Enquanto permanecia assim, diligente, ardente e decidido, um
pensamento de não má vontade surgiu em mim… um pensamento de não crueldade
surgiu em mim. <span style='mso-spacerun:yes'> </span>Eu compreendi desta
forma: ‘Este pensamento de não crueldade surgiu em mim. Isso não conduz à minha
própria aflição, ou à aflição dos outros, ou à aflição de ambos; isso auxilia a
sabedoria, não causa dificuldades e conduz a Nibbana. Se eu pensar e ponderar
esse pensamento mesmo que seja por uma noite, mesmo por um dia, mesmo por uma
noite e dia, eu não vejo nada que temer. Mas com o excessivo pensar e ponderar
eu poderei cansar meu corpo e quando o corpo fica cansado, a mente fica tensa e
quando a mente fica tensa ela fica muito distante da concentração.’ Assim eu
estabilizei a minha mente internamente, tranqüilizei-a, eu a unifiquei e concentrei. Por que isso? Para que a minha mente não ficasse
exausta.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11.
“Bhikkhus, qualquer coisa que um bhikkhu pense e pondere com freqüência, essa
passará a ser a tendência da sua mente. Se ele pensar e ponderar com freqüência
pensamentos de renúncia, ele terá abandonado o pensamento de desejo sensual
para cultivar o pensamento de renúncia e então a sua mente irá tender para o
pensamento de renúncia. Se ele pensar e ponderar com freqüência pensamentos de
não má vontade … pensamentos de não crueldade, ele terá abandonado o pensamento
da crueldade para cultivar o pensamento de não crueldade e então a sua mente
irá tender para os pensamentos de não crueldade.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. “Da
mesma maneira como no último mês da estação quente, quando todas as colheitas
foram concluídas, um vaqueiro cuida dos seus bois à sombra de uma árvore ou no
campo aberto, já que ele apenas precisa estar ciente que os bois ali estão;
assim também, apenas era necessário que eu estivesse ciente que aqueles estados ali
estavam.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. “A
energia infatigável foi desperta em mim e a atenção plena perseverante foi estabelecida,
meu corpo estava tranqüilo e sossegado, minha mente concentrada e unificada.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14-23.
“Totalmente afastado dos prazeres sensuais, afastado das qualidades não hábeis,
eu entrei e permaneci no primeiro jhana...(igual ao </span><a href="MN4.php#T23"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>MN 4, versos 23-32</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>)...Eu compreendi que ‘O nascimento
foi destruído, a vida santa foi vivida, o que devia ser feito foi feito, não há
mais vir a ser a nenhum estado.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>24. “Esse
foi o terceiro conhecimento verdadeiro que alcancei na terceira vigília da
noite. A ignorância foi extirpada e surgiu o verdadeiro conhecimento, a
escuridão foi extinta e surgiu a luz, como ocorre com aquele que permanece
diligente, ardente e decidido.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><a name=T25></a>25.
“Suponham, bhikkhus, que em uma floresta houvesse uma grande área pantanosa
onde vivesse um grande rebanho de gamos. Então, aparecesse um homem que
desejasse a ruína deles, desejasse prejudicá-los e aprisioná-los; e ele
fechasse o caminho bom e seguro que os conduziria à felicidade e abrisse um
caminho falso e colocasse uma armadilha com uma figura para que o grande rebanho
de gamos mais tarde desse de encontro com uma grande calamidade, desastre e
perda. Mas um outro homem desejando o bem deles, o bem-estar e proteção deles,
reabrisse o caminho bom e seguro que os conduziria à felicidade, fechasse o
caminho falso e removesse a armadilha e destruísse a figura para que o grande
rebanho de gamos mais tarde pudesse crescer, aumentar e se realizar.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>26.
“Bhikkhus, eu citei este símile para transmitir uma idéia. A idéia é a seguinte:
‘Grande área pantanosa’ é um termo para os prazeres sensuais. ‘Grande rebanho
de gamos’ é um termo para os seres. ‘Um homem que desejasse a ruína deles, que
desejasse prejudicá-los e aprisioná-los’ é um termo para Mara o Senhor do Mal.
‘Caminho falso’ é um termo para o caminho óctuplo incorreto, isto é:
entendimento incorreto, pensamento incorreto, linguagem incorreta, ação
incorreta, modo de vida incorreto, esforço incorreto, atenção plena incorreta e
concentração incorreta. ‘Armadilha’ é um termo para o deleite e cobiça.
‘Figura’ é um termo para ignorância. ‘Um homem desejando o bem deles, o
bem-estar e proteção deles’ é um termo para o Tathagata, o arahant,
perfeitamente iluminado. ‘O caminho bom e seguro’ é um termo para o Nobre
Caminho Óctuplo, isto é: entendimento correto, pensamento correto, linguagem
correta, ação correta, modo de vida correto, esforço correto, atenção plena
correta e concentração correta. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
bhikkhus, o caminho bom e seguro que conduz à felicidade foi reaberto por mim, o
caminho falso foi fechado, a armadilha removida, a figura destruída.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>27. “Aquilo
que por compaixão um Mestre deveria fazer para os seus discípulos, desejando o
bem-estar deles, isso eu fiz por vocês, bhikkhus. Ali estão aquelas 
árvores, aquelas cabanas vazias. Meditem, bhikkhus, não adiem, ou vocês se
arrependerão mais tarde. Essa é a nossa instrução para vocês.”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o
que disse o Abençoado. Os bhikkhus ficaram satisfeitos e contentes com as
palavras do Abençoado. <o:p></o:p></span></p>

