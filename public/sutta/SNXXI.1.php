<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim ouvi.
Em certa ocasião, o Abençoado estava em Savatthi no Bosque de Jeta, no
Parque de Anathapindika. Lá o Venerável Mahamoggallana se dirigiu aos monges
desta forma: “Amigos, bhikkhus” – “Amigo,” eles responderam. O Venerável
Mahamoggallana disse o seguinte: <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
amigos, enquanto eu estava só em retiro, este pensamento surgiu na minha mente:
‘É dito, “nobre silêncio, nobre silêncio.” O que é o nobre silêncio?’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
amigos, me ocorreu: ‘Aqui, silenciando o pensamento aplicado e sustentado, um
bhikkhu entra e permanece no segundo jhana, que é acompanhado pela auto-confiança
e unicidade da mente sem o pensamento aplicado e sustentado, com o êxtase e
felicidade nascidos da concentração. Isso é chamado nobre silêncio.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
amigos, silenciando o pensamento aplicado e sustentado, eu entrei e permaneci
no segundo jhana, que é acompanhado pela auto-confiança e unicidade da mente
sem o pensamento aplicado e sustentado, com o êxtase e felicidade nascidos da
concentração. Enquanto ali permanecia, a percepção e a atenção acompanhadas
pelo pensamento me invadiram.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
amigos, o Abençoado veio até mim por meio dos poderes supra-humanos e disse:
‘Moggallana, Moggallana, não seja negligente com relação ao nobre silêncio,
brâmane. Estabilize a sua mente no nobre silêncio, unifique a sua mente no
nobre silêncio, concentre a sua mente no nobre silêncio.’ Então, amigos, mais
tarde, numa outra ocasião, silenciando o pensamento aplicado e sustentado, eu
entrei e permaneci no segundo jhana, que é acompanhado pela auto-confiança e
unicidade da mente sem o pensamento aplicado e sustentado, com o êxtase e
felicidade nascidos da concentração.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se,
amigos, falando corretamente, fosse para dizer de alguém que: ‘Ele é um
discípulo que alcançou a grandeza do conhecimento direto com o auxílio do
Mestre,’ é de mim verdadeiramente que, falando o que é certo, isso deveria ser
dito.” <b style='mso-bidi-font-weight:normal'><o:p></o:p></b></span></p>


