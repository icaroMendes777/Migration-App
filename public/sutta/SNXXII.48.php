chamamos o agregado da percepção.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Toda e
qualquer formação volitiva, quer seja do passado, futuro ou presente; interna ou
