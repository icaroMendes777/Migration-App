chamamos o agregado das formações volitivas.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Toda e
qualquer consciência, quer seja do passado, futuro ou presente; interna ou
