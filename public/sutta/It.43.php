<p class=Normal> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: " Existe, bhikkhus, o que não nasceu - o que não é - o que não é fabricado - o que não é condicionado. Se não existisse o que não nasceu - o que não é - o que não é fabricado - o que não é condicionado, não haveria a situação na qual a emancipação do nascido - do que é - do fabricado - do condicionado seria discernida. Porém precisamente porque há o que não nasceu - o que não é - o que não é fabricado - o que não é condicionado, a emancipação do nascido - do que é - do fabricado - do condicionado é discernida."</p>


<p class=Normal style='margin-left:.75in'> O nascido, que veio a ser, produzido, <br>
fabricado, condicionado, transitório, <br>
unido ao envelhecimento e morte, <br>
um ninho de enfermidades, perecível, <br>
surgido do alimento e do desejo -<br>
inadequado para o deleite.</p>

<p class=Normal style='margin-left:.75in'> A escapatória disso tudo, <br>
está além do raciocínio, <br>
não nascido, não produzido, <br>
 o estado livre da tristeza, imaculado, <br>
a cessação dos estados conectados ao sofrimento, <br>
o silenciar do condicionado -<br>
bem-aventurança.</p>


