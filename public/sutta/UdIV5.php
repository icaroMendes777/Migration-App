<p class=Normal>Assim ouvi. Em certa ocasião, o Abençoado estava em Kosambi, no monastério de Ghosita. </p>



<p class=Normal>Agora, naquela ocasião o Abençoado se encontrava rodeado por bhikkhus e bhikkhunis, por discípulos leigos e leigas, por reis e ministros, por líderes de outras seitas e seus discípulos, e ele vivia em desconforto, assolado e sem tranquilidade.</p> 



<p class=Normal>Então o seguinte pensamento ocorreu ao Abençoado: "Eu agora me encontro rodeado por bhikkhus e bhikkhunis, por discípulos leigos e leigas, por reis e ministros, por líderes de outras seitas e seus discípulos, eu vivo em desconforto, assolado e sem tranquilidade. Suponha que eu fosse viver só, afastado da multidão?”</p>



<p class=Normal>Então, ao amanhecer, o Abençoado se vestiu e tomando a tigela e o manto externo, foi para Kosambi para esmolar alimentos. Depois de haver esmolado em Kosambi e de haver retornado, após a refeição ele arrumou a sua habitação e – sem chamar o seu assistente ou avisar a comunidade de bhikkhus – tomou a tigela e o manto externo e saiu caminhando só e sem companhia, rumo à Palileyyaka. Então, depois de caminhar durante algum tempo, o Abençoado chegou em Palileyyaka. Lá, num bosque isolado, ele sentou ao pé de uma auspiciosa árvore Sal.</p>



<p class=Normal>Agora, naquela mesma ocasião, um certo elefante Naga se encontrava rodeado de elefantes e elefantas, jovens elefantes e filhotes de elefantes. Ele se alimentava de grama partida, enquanto que os demais comiam aquilo que ele arrancava dos galhos mais altos. Ele bebia água turva e agitada. E quando ele terminava seus banhos as elefantas o seguiam, esbarrando em seu corpo. Rodeado, ele vivia em desconforto, assolado e sem tranquilidade.<br> 



<p class=Normal>Então o seguinte pensamento ocorreu ao elefante Naga: "Eu agora me encontro rodeado de elefantes e elefantas, jovens elefantes e filhotes de elefantes. Eu me alimento de grama partida, enquanto que os demais comem aquilo que eu arranco dos galhos mais altos. Eu bebo água turva e agitada. E quando eu termino meus banhos as elefantas me seguem, esbarrando em meu corpo. Rodeado, eu vivo em desconforto, assolado e sem tranquilidade. Suponha que eu fosse viver só, afastado da multidão?”</p>



<p class=Normal>Então, o elefante Naga, deixando o seu rebanho de elefantes, seguiu rumo à Palileyyaka, para um bosque isolado, ao pé de uma auspiciosa árvore Sal - exatamente onde se encontrava o Abençoado. Lá, ele aparava a grama e capinava o mato nos arredores do local, trazendo para o Abençoado, com sua tromba, água para o consumo e para o banho.</p>



<p class=Normal>Então, enquanto o Abençoado se encontrava só, o seguinte pensamento lhe ocorreu: "Antes eu vivia rodeado por bhikkhus e bhikkhunis, por discípulos leigos e leigas, por reis e ministros, por líderes de outras seitas e seus discípulos. Rodeado eu vivia em desconforto, assolado e sem tranquilidade. Mas agora e não vivo rodeado por bhikkhus e bhikkhunis, por discípulos leigos e leigas, por reis e ministros, por líderes de outras seitas e seus discípulos. Afastado, eu vivo em conforto e com tranquilidade.”</p>



<p class=Normal>E o seguinte pensamento ocorreu ao elefante Naga: "Antes eu me encontrava rodeado de elefantes e elefantas, jovens elefantes e filhotes de elefantes. Eu me alimentava de grama partida, enquanto que os demais comiam aquilo que eu arrancava dos galhos mais altos. Eu bebia água turva e agitada. E quando eu terminava meus banhos as elefantas me seguiam, esbarrando em meu corpo. Rodeado, eu vivia em desconforto, assolado e sem tranquilidade. Mas agora eu não me encontro rodeado de elefantes e elefantas, jovens elefantes e filhotes de elefantes. Eu me alimento de grama de boa qualidade. Eles não comem o que eu arranco dos galhos mais altos. Eu bebo água limpa, não agitada. E quando eu termino meus banhos as elefantas não me seguem, esbarrando em meu corpo. Afastado, eu vivo em conforto e com tranqulidade.</p>



<p class=Normal>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p>



	<p class=Normal style='margin-left:.75in'>"Nisto concordam duas mentes e corações -<br>

	do Abençoado e do elefante Naga com  presas majestosas -<br>

	ambos se deleitam afastados, na floresta.”</p>
