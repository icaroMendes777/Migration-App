<p class=Normal><a name=N4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[4] Os oito fatores do caminho mencionados parecem fazer parte da porção
preliminar ou mundana do caminho. MT os identifica com os fatores possuídos por
uma pessoa no nível mais alto do desenvolvimento de</span> insight,
imediatamente antes do surgimento do caminho supramundano. Neste estágio,
apenas os cinco primeiros fatores do caminho estão ativos, sendo que os três
fatores do grupo da virtude já foram purificados antes de se empenhar na
meditação de insight. Mas quando o caminho supramundano surge, todos os oito
fatores ocorrem de forma simultânea, e os três fatores do grupo da virtude
exercem a função de erradicantes das contaminações responsáveis pelas
transgressões através da linguagem, ação e modo de vida. [<a href="#R4">Retorna</a>]<span
lang=PT-BR style='font-size:11.0pt;mso-bidi-font-size:12.0pt;mso-ansi-language:
PT-BR'><o:p></o:p></span></p>


<p class=Normal><a name=N5></a> [5] </span>MA diz que isto
se refere ao surgimento simultâneo da tranqüilidade e insight no caminho
supramundano. O primeiro está presente sob a concentração correta e o último
sob o entendimento correto. [<a href="#R5">Retorna</a>]<span lang=PT-BR
style='mso-ansi-language:PT-BR'><o:p></o:p></span></p>


<p class=Normal><a name=N6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[6] Essas são as quatro funções exercidas pelo caminho supramundano: completa
compreensão da verdade do sofrimento, abandono da causa do sofrimento,
realização da cessação do sofrimento e desenvolvimento do caminho que conduz ao
fim do sofrimento. </span>[<a href="#R6">Retorna</a>]<span lang=PT-BR
style='mso-ansi-language:PT-BR'><o:p></o:p></span></p>


<p class=Normal><a name=N7></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[7] Aqui tranqüilidade e </span>insight representam todo o Nobre Caminho
Óctuplo. [<a href="#R7">Retorna</a>] <span lang=PT-BR style='mso-ansi-language:
PT-BR'><o:p></o:p></span></p>

<p class=Normal><a name=N8></a><span lang=PT-BR style='mso-ansi-language:
