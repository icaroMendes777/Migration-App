<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[2] Isto é, o desejo que surge e se estabelece no olho, nas formas,
etc., se agarra a eles através do apego, e isso resulta em uma acumulação de
kamma que tende a gerar um novo conjunto de cinco agregados na existência
seguinte. </span>[<a href="#R2">Retorna</a>]<span lang=PT-BR style='mso-ansi-language:
