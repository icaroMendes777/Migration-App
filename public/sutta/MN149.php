<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião o Abençoado estava em Savatthi, no Bosque de Jeta, no
Parque de Anathapindika. </span>Lá ele se dirigiu aos monges desta forma:
“Bhikkhus.” – “Venerável Senhor,” eles responderam. O Abençoado disse o
seguinte:</p>


<p class=Normal>2. “Bhikkhus, eu ensinarei para vocês o grande discurso das
seis<span style="mso-spacerun: yes">  </span>bases. Ouçam e prestem muita
atenção ao que vou dizer.” – “Sim, venerável senhor,” os bhikkhus
responderam. O Abençoado disse o seguinte:</p>


<p class=Normal>3. “Bhikkhus, quando alguém não compreende e não vê o olho
como na verdade ele é, [<a href="#N1">1</a>] <a name=R1></a>quando alguém não
compreende e não vê as formas como na verdade elas são, quando alguém não
compreende e não vê a consciência no olho como na verdade ela é, quando alguém
não compreende e não vê o contato no olho como na verdade ele é, quando alguém
não compreende e não vê como na verdade é a sensação sentida como prazerosa, ou
dolorosa, ou nem prazerosa, nem dolorosa, que surge tendo o contato no olho
como condição, então ele está em chamas pela cobiça pelo olho, pelas formas,
pela consciência no olho, pelo contato no olho, pela sensação sentida como
prazerosa, ou dolorosa, ou nem prazerosa, nem dolorosa, que surge tendo o
contato no olho como condição.</p>


<p class=Normal>“Para ele – apaixonado, agrilhoado, confuso, sem permanecer 
focado nas suas desvantagens – os cinco agregados influenciados pelo apego estão 
direcionados a uma acumulação no futuro. [<a href="#N2">2</a>] <a name=R2></a>O desejo – 
que conduz a uma renovada existência, acompanhado pela cobiça e pelo prazer, buscando o 
prazer aqui e ali – aumenta nele. As suas perturbações corporais e mentais aumentam. Os seus 
tormentos corporais e mentais aumentam. As suas febres corporais e mentais aumentam. Ele 
experimenta ambos a dor corporal e a mental.</p>


<p class=Normal>4-8. “Quando alguém não compreende e não vê o ouvido como na
verdade ele é ... Quando alguém não compreende e não vê o nariz como na verdade
ele é ... Quando alguém não compreende e não vê a língua como na verdade ela é
... Quando alguém não compreende e não vê o corpo como na verdade ele é ...
Quando alguém não compreende e não vê a mente como na verdade ela é ... ele
experimenta ambos a dor corporal e a mental.</p>



<p class=Normal>9. “Bhikkhus, quando alguém compreende e vê o olho como na
verdade ele é, [<a href="#N3">3</a>] <a name=R3></a>quando alguém compreende e
vê as formas como na verdade elas são, quando alguém compreende e vê a
consciência no olho como na verdade ela é, quando alguém compreende e vê o
contato no olho como na verdade ele é, quando alguém compreende e vê como na
verdade é a sensação sentida como prazerosa, ou dolorosa, ou nem prazerosa, nem
dolorosa, que surge tendo o contato no olho como condição, então ele não está
em chamas pela cobiça pelo olho, pelas formas, pela consciência no
olho, pelo contato no olho, pela sensação sentida como prazerosa, ou dolorosa,
ou nem prazerosa, nem dolorosa, que surge tendo o contato no olho como
condição.</p>



<p class=Normal>“Para ele – sem estar apaixonado, sem estar agrilhoado, sem estar confuso, permanecendo 
focado nas suas desvantagens – os cinco agregados influenciados pelo apego não estão 
direcionados a uma acumulação no futuro. O desejo – 
que conduz a uma renovada existência, acompanhado pela cobiça e pelo prazer, buscando o 
prazer aqui e ali – é abandonado. As suas perturbações corporais e mentais são abandonadas. Os seus 
tormentos corporais e mentais são abandonados. As suas febres corporais e mentais são abandonadas.
 Ele experimenta ambos o prazer corporal e o mental.</p>



<p class=Normal><a name=T10></a>10. “O entendimento de alguém assim é o
entendimento correto. O seu pensamento é o pensamento correto, o seu esforço é
o esforço correto, a sua atenção plena é a atenção plena correta, a sua
concentração é a concentração correta. Mas a sua ação corporal, a sua ação
verbal e o seu modo de vida já foram bem purificados antes. [<a href="#N4">4</a>]
<a name=R4></a>Assim, este Nobre Caminho Óctuplo é<span style="mso-spacerun:
yes">  </span>realizado por ele através do desenvolvimento. Ao desenvolver este
Nobre Caminho Óctuplo, os quatro fundamentos da atenção plena também são
realizados por ele através do seu desenvolvimento; os quatro tipos de esforço correto
também são realizados por ele através do desenvolvimento; as quatro bases do
poder espiritual também são realizadas por ele através do desenvolvimento; as
cinco faculdades também são realizadas por ele através do desenvolvimento; os
cinco poderes também são realizados por ele através do desenvolvimento; os sete
fatores da iluminação também são realizados por ele através do desenvolvimento.
Essas duas coisas – tranqüilidade e insight – ocorrem juntas, emparelhadas em
igualdade. [<a href="#N5">5</a>] <a name=R5></a>Ele compreende por completo
através do conhecimento direto aquelas coisas que devem ser compreendidas por
completo através do conhecimento direto. Ele abandona através do conhecimento
direto aquelas coisas que devem ser abandonadas através do conhecimento direto.
Ele desenvolve através do conhecimento direto aquelas coisas que devem ser
desenvolvidas através do conhecimento direto. Ele realiza através do
conhecimento direto aquelas coisas que devem ser realizadas através do
conhecimento direto.[<a href="#N6">6</a>]</p>



<p class=Normal><a name=R6></a>11. “E quais são as coisas que devem ser
compreendidas através do conhecimento direto? A resposta a isso é: os cinco
agregados influenciados pelo apego, isto é, o agregado da forma material
influenciado pelo apego, o agregado da sensação influenciado pelo apego, o
agregado da percepção influenciado pelo apego, o agregado das formações
influenciado pelo apego, o agregado da consciência influenciado pelo apego.
Essas são as coisas que devem ser compreendidas através do conhecimento direto.</p>



<p class=Normal><span style="mso-spacerun: yes"> </span>“E quais são as coisas
que devem ser abandonadas através do conhecimento direto? Ignorância e desejo
por ser/existir. Essas são as coisas que devem ser abandonadas através do
conhecimento direto.</p>



<p class=Normal>“E quais são as coisas que devem ser desenvolvidas através
do conhecimento direto? Tranqüilidade e insight.[<a href="#N7">7</a>] <a
name=R7></a>Essas<span style="mso-spacerun: yes">  </span>são as coisas que
devem ser desenvolvidas através do conhecimento direto.</p>



<p class=Normal>“E quais são as coisas que devem ser realizadas através do
conhecimento direto? O verdadeiro conhecimento e libertação.[<a href="#N8">8</a>]
<a name=R8></a>Essas são as coisas que devem ser realizadas através do
conhecimento direto.</p>



<p class=Normal>12-14. “Quando alguém compreende e vê o ouvido como na
verdade ele é ... Essas são as coisas que devem ser realizadas através do
conhecimento direto.[<a href="#N9">9</a>]</p>



<p class=Normal><a name=R9></a>15-17. “Quando alguém compreende e vê o nariz
como na verdade ele é ... Essas são as coisas que devem ser realizadas através
do conhecimento direto.</p>



<p class=Normal>18-20. “Quando alguém compreende e vê a língua como na
verdade ela é ... Essas são as coisas que devem ser realizadas através do
conhecimento direto.</p>



<p class=Normal>21-23. “Quando alguém compreende e vê o corpo como na
verdade ele é ... Essas são as coisas que devem ser realizadas através do
conhecimento direto.</p>



<p class=Normal>24-26. “Quando alguém compreende e vê a mente como na
verdade ela é ... Essas são as coisas que devem ser realizadas através do
conhecimento direto.”</p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o
que disse o Abençoado. Os bhikkhus ficaram satisfeitos e contentes com as
palavras do Abençoado. <o:p></o:p></span></p>


