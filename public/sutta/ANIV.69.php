<p class=Normal> "Bhikkhus, há esses quatro esforços. Quais quatro? Esforço através da contenção, esforço através do abandono, esforço através da meditação, esforço através da preservação. </p>

<p class=Normal> (1) "E como, bhikkhus, é o esforço através da contenção? Aqui, um bhikkhu gera desejo para que não surjam estados ruins e prejudiciais que ainda não surgiram e ele se aplica, estimula a sua energia, empenha a sua mente e se esforça. Esse é chamado o esforço através da contenção. </p>

<p class=Normal> (2) "E como é o esforço através do abandono? Aqui, um bhikkhu gera desejo em abandonar estados ruins e prejudiciais que já surgiram e ele se aplica, estimula a sua energia, empenha a sua mente e se esforça. Esse é chamado o esforço através do abandono. </p>

<p class=Normal> (3) "E como é o esforço através da meditação? Aqui, um bhikkhu gera desejo para que surjam estados benéficos que ainda não surgiram e ele se aplica, estimula a sua energia, empenha a sua mente e se esforça. Esse é chamado o esforço através da meditação. </p>

<p class=Normal> (4) "E como é o esforço através da preservação? Aqui, um bhikkhu gera desejo para a continuidade, o não desaparecimento, o fortalecimento, o incremento e a realização através do desenvolvimento de estados benéficos que já surgiram e ele se aplica, estimula a sua energia, empenha a sua mente e se esforça. Esse é chamado o esforço através da preservação. </p>

<p class=Normal> "Esses são os quatro esforços." </p>

<p class=Normal> Contenção e abandono,<br>
meditação e preservação:<br>
através desses quatro esforços<br>
ensinados pelo Parente do Sol<br>
um bhikkhu que é ardente pode aqui<br>
realizar a destruição do sofrimento.</p>

