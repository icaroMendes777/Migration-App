<p class=Normal> Ouvi que certa vez o Abençoado estava em Nadika na Casa de Tijolos. Lá ele se dirigiu aos bhikkhus: “Bhikkhus!" </p>

<p class=Normal> "Venerável senhor!" os bhikkhus responderam. O Abençoado disse o seguinte: </p>

<p class=Normal> “Bhikkhus, atenção plena na morte, quando desenvolvida e cultivada, traz grandes frutos e grandes benefícios. Ela mergulha no Imortal, possui o Imortal como seu objetivo final. Bhikkhus, vocês desenvolvem a atenção plena na morte?" </p>

<p class=Normal> (1) Quando isso foi dito, um certo bhikkhu se dirigiu ao Abençoado, “Eu desenvolvo a atenção plena na morte.” </p>

<p class=Normal> “E como você desenvolve a atenção plena na morte?” </p>

<p class=Normal> “Eu penso, ‘Que eu possa viver por um dia e uma noite, que eu possa seguir as instruções do Abençoado. Eu teria realizado muito.’ Assim é como desenvolvo a atenção plena na morte.” </p>

<p class=Normal> (2) Então um outro bhikkhu se dirigiu ao Abençoado, “Eu, também desenvolvo a atenção plena na morte.” </p>

<p class=Normal> “E como você desenvolve a atenção plena na morte?” </p>

<p class=Normal> “Eu penso, ‘Que eu possa viver por um dia, que eu possa seguir as instruções do Abençoado. Eu teria realizado muito.’ Assim é como desenvolvo a atenção plena na morte. </p>

<p class=Normal> (3) Então um outro bhikkhu se dirigiu ao Abençoado, “Eu, também desenvolvo a atenção plena na morte.” </p>

<p class=Normal> “E como você desenvolve a atenção plena na morte?” </p>

<p class=Normal> “Eu penso, ‘Que eu possa viver por meio dia, que eu possa seguir as instruções do Abençoado. Eu teria realizado muito.’ Assim é como desenvolvo a atenção plena na morte. </p>

<p class=Normal> (4) Então um outro bhikkhu se dirigiu ao Abençoado, “Eu, também desenvolvo a atenção plena na morte.”... “Eu penso, ‘Que eu possa viver o tempo que tarda para comer uma refeição, que eu possa seguir as instruções do Abençoado. Eu teria realizado muito.’…”</p>

<p class=Normal> (5) Então um outro bhikkhu se dirigiu ao Abençoado, “Eu, também desenvolvo a atenção plena na morte.”... “Eu penso, ‘Que eu possa viver o tempo que tarda para comer metade de uma refeição, que eu possa seguir as instruções do Abençoado. Eu teria realizado muito.’…”</p>

<p class=Normal> (6) Então um outro bhikkhu se dirigiu ao Abençoado, “Eu, também desenvolvo a atenção plena na morte.”... “Eu penso, ‘Que eu possa viver o tempo que tarda para engolir, depois de mastigar, quatro bocados de comida, que eu possa seguir as instruções do Abençoado. Eu teria realizado muito.’…”</p>

<p class=Normal> (7) Então um outro bhikkhu se dirigiu ao Abençoado, “Eu, também desenvolvo a atenção plena na morte.”... “Eu penso, ‘Que eu possa viver o tempo que tarda para engolir, depois de mastigar, um bocado de comida, que eu possa seguir as instruções do Abençoado. Eu teria realizado muito.’…”</p>

<p class=Normal> (8) Então um outro bhikkhu se dirigiu ao Abençoado, “Eu, também desenvolvo a atenção plena na morte.”... “Eu penso, ‘Que eu possa viver o tempo que tarda para expirar depois de haver inspirado ou de inspirar depois de haver expirado, que eu possa seguir as instruções do Abençoado. Eu teria realizado muito.’…”</p>

<p class=Normal> Quando isso foi dito, o Abençoado se dirigiu aos bhikkhus: "Bhikkhus, o bhikkhu que desenvolve a atenção plena na morte assim: que eu possa viver por um dia e uma noite ... por um dia ... por meio dia ... o tempo que tarda para comer uma refeição ... o tempo que tarda para comer meia refeição ... o tempo que tarda para engolir, depois de mastigar, quatro bocados de comida, que eu possa seguir as instruções do Abençoado. Eu teria realizado muito’ – eles são chamados bhikkhus que permanecem negligentes. Eles desenvolvem a atenção plena na morte com lentidão, com o propósito de dar um fim às impurezas. </p>

<p class=Normal> “Mas o bhikkhu que desenvolve a atenção plena na morte assim: ‘que eu possa, viver o tempo que tarda para engolir, depois de mastigar, um bocado de comida ... viver o tempo que tarda para expirar depois de haver inspirado ou de inspirar depois de haver expirado, que eu possa seguir as instruções do Abençoado. Eu teria realizado muito’ – eles são chamados bhikkhus que permanecem diligentes. Eles desenvolvem a atenção plena na morte de modo penetrante, com o propósito de dar um fim às impurezas.” </p>

<p class=Normal> “Portanto, assim vocês devem treinar: ‘Nós permaneceremos diligentes. Nós iremos desenvolver a atenção plena na morte de modo penetrante, com o propósito de dar um fim às impurezas.’ Assim é como vocês deveriam treinar.”
</p>

