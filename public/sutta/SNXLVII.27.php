<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'> Em certa ocasião, o Venerável Sariputta e o Venerável Mahamoggallana e o Venerável Anuruddha estavam em Saketa no Bosque dos Arbustos Espinhosos. Então, ao anoitecer, o Venerável Sariputta e o Venerável Mahamoggallana saíram do seu isolamento e foram até o Venerável Anuruddha e eles se cumprimentaram. Quando a conversa cortês e amigável havia terminado, eles sentaram a um lado e o Venerável Sariputta disse para o Venerável Anuruddha:<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'> “Amigo Anuruddha, dizem, ‘Alguém além do treinamento, Alguém além do treinamento.’ De que modo, amigo, alguém está além do treinamento?” <o:p></o:p></span></p>


<p class=Normal> “Amigo, é porque ele desenvolveu completamente os quatro fundamentos da atenção plena que ele está além do treinamento. Quais quatro? Neste caso, amigos, um bhikkhu permanece contemplando o corpo como um corpo, ardente, plenamente consciente e com atenção plena, tendo colocado de lado a cobiça e o desprazer pelo mundo. Ele permanece contemplando as sensações como sensações ... mente como mente ... objetos mentais como objetos mentais, ardente, plenamente consciente e com atenção plena, tendo colocado de lado a cobiça e o desprazer pelo mundo. Porque ele desenvolveu completamente os quatro fundamentos da atenção plena é que ele está além do treinamento.”<o:p></o:p></span></p>

