<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[1] <i>Cetokhila</i>: <i>khila</i> significa um pedaço de terra não cultivado, <i>cetokhila</i> então poderia ser interpretado como a mente que não é cultivada.  MA explica <i>cetokhila</i> como bloqueio, rigidez, tontice ou embrutecimento
da mente. <i>Cetaso vinibandha</i> como algo que ata a mente,
agarrando-a como um punho cerrado, por conseguinte “grilhões  na mente.” O
primeiro como será visto, consiste de quatro situações de dúvida e uma de
raiva; e o último de cinco variedades de cobiça. [<a href="#R1">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[2] MA explica “Dhamma” aqui como os
ensinamentos das escrituras e a realização dos caminhos supramundanos, seus
frutos e Nibbana. O Dhamma como prática é mencionado em separado logo em
seguida como o treinamento (<i>sikkha</i>) – isto é, o treinamento tríplice em
virtude, concentração e sabedoria. [<a href="#R2">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[3] “Corpo” aqui é o próprio corpo dele,
enquanto que “forma” mencionado logo a seguir são as formas externas, os corpos
dos outros. [<a href="#R3">Retorna</a>] <o:p></o:p></span></p>


<p class=Normal><a name=N4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[4] As quatro bases para o poder espiritual, (<i>iddhipada</i>),
estão incluídas entre os trinta e sete apoios para a iluminação; elas são o
fundamento especial para os cinco tipos de conhecimentos supra-humanos mundanos,
(<i>abhiñña</i>). De acordo com MA, o entusiasmo, (<i>ussolhi</i>), é a energia,
que deve ser aplicada em tudo. [<a href="#R4">Retorna</a>] <o:p></o:p></span></p>


