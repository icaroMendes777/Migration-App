<p class=Normal>
Ouvi que certa vez o Abençoado estava em<span style="mso-spacerun:
yes">  </span>Nadika na Casa de Tijolos. Lá ele se dirigiu aos bhikkhus: <o:p></o:p></span></p>


<p class=Normal>
“Bhikkhus, a atenção plena na morte, quando desenvolvida e cultivada, traz
grandes frutos e grandes benefícios. Ela mergulha no Imortal, possui o Imortal
como seu objetivo final. E como a atenção plena na morte é desenvolvida e cultivada
para que traga grandes frutos e grandes benefícios, mergulhe no Imortal, e
tenha o Imortal como seu objetivo final? <o:p></o:p></span></p>


<p class=Normal>
&quot;É o caso em que um bhikkhu, terminando o dia e chegando a noite,
reflete: ‘Muitas são as [possíveis] causas da minha morte. (1) Uma cobra pode me
morder, um escorpião pode me ferroar, uma centopéia pode me morder;
(2) tropeçando, eu posso cair; (3) minha comida, digerida, pode me causar problemas;
(4) minha bílis pode ser provocada; (5) minha fleuma pode ser provocada; (6) forças perfurantes do vento [no
corpo] podem ser provocadas. Assim é como a minha morte poderia acontecer. Isso
seria um impedimento para mim.’ 

<p class=Normal>
&quot;Então o bhikkhu deveria investigar: ‘Existem
qualidades mentais inábeis e prejudiciais que não foram abandonadas por mim e
que seriam um impedimento para mim caso eu morresse durante a noite?’ Se,
refletindo, ele se dá conta que qualidades mentais inábeis e prejudiciais não
abandonadas por ele seriam um impedimento caso ele morresse durante a noite,
então ele deve empregar desejo adicional, empenho, diligência, esforço, atenção
plena e plena consciência para o abandono dessas mesmas qualidades inábeis e
prejudiciais. Tal como se o turbante ou o cabelo de uma pessoa estivesse em
chamas ela empregaria desejo adicional, empenho, diligência, esforço, atenção
plena e plena consciência para apagar o fogo do seu turbante ou cabelo, da
mesma forma o bhikkhu deveria empregar<span style="mso-spacerun: yes"> 
</span>desejo adicional, empenho, diligência, esforço, atenção plena e plena
consciência<span style="mso-spacerun: yes">  </span>para o abandono dessas
mesmas qualidades inábeis e prejudiciais. 

<p class=Normal>
&quot;Porém, se refletindo ele se dá conta
que não há qualidades inábeis e prejudiciais que não foram abandonadas por ele
que seriam um impedimento se ele morresse durante a noite, então por essa mesma
razão ele deve permanecer em êxtase e felicidade, treinando dia e noite nas qualidades
hábeis. <o:p></o:p></span></p>

<p class=Normal>
&quot;Além disso, há o caso em que um bhikkhu, terminando a noite e
chegando o dia, reflete: ‘Muitas são as [possíveis] causas da minha morte. Uma
cobra pode me morder, ... forças perfurantes do
vento [no corpo] podem ser provocadas. Assim é como a minha morte poderia
acontecer. Isso seria um impedimento para mim.’ 

<p class=Normal>
&quot;Então o bhikkhu deveria
investigar. ‘ Existem qualidades mentais inábeis e prejudiciais que não foram
abandonadas por mim e que seriam um impedimento para mim caso eu morresse
durante o dia?’ Se, refletindo, ele se dá conta de que qualidades mentais
inábeis e prejudiciais não abandonadas por ele seriam um impedimento caso ele
morresse durante o dia, então ele deve empregar desejo adicional, empenho,
diligência, esforço, atenção plena e plena consciência para o abandono dessas
mesmas qualidade inábeis e prejudiciais. Tal como se o turbante ou o cabelo de
uma pessoa estivesse em chamas ela empregaria desejo adicional, empenho,
diligência, esforço, atenção plena e plena consciência para apagar o fogo do
seu turbante ou cabelo, da mesma forma o bhikkhu deveria empregar desejo
adicional, empenho, diligência, esforço, atenção plena não dividida e
vigilância<span style="mso-spacerun: yes">  </span>para o abandono dessas
mesmas qualidades inábeis e prejudiciais. 

<p class=Normal>
&quot;Porém, se refletindo ele se dá conta
que não há qualidades inábeis e prejudiciais que não foram abandonadas por ele
que seriam um impedimento se ele morresse durante o dia, então por essa mesma
razão ele deve permanecer em êxtase e felicidade, treinando dia e noite nas
qualidades hábeis.<o:p></o:p></span></p>


<p class=Normal>
&quot;Dessa forma, bhikkhus, é como a atenção plena na morte é
desenvolvida e cultivada de forma a trazer grandes frutos e grandes benefícios.
Ela mergulha no Imortal, possui o Imortal como seu objetivo final.&quot; <o:p></o:p></span></p>
