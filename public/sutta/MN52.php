cumprimentá-lo sentou a um lado e perguntou: “Onde está agora o venerável
Ananda, venerável senhor? Eu gostaria de ver o venerável Ananda.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O
venerável Ananda está em Beluvagamaka próximo a Vesali, chefe de família.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. Quando o
chefe de família Dasama havia concluído os seus negócios em Pataliputta, ele
foi até o venerável Ananda em Beluvagamaka próximo a Vesali. Depois de
cumprimentá-lo ele sentou a um lado e perguntou:<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
