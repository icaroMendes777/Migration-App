<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Qual é
essa coisa, venerável Ananda?”<o:p></o:p></span></p>



<p class=Normal><a name=T4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>4. “Aqui, chefe de família, um bhikkhu afastado dos prazeres sensuais, afastado das qualidades não hábeis, entra e permanece no primeiro jhana, que é caracterizado pelo pensamento aplicado e sustentado, com o êxtase e felicidade nascidos do afastamento. Ele considera e entende isso da seguinte forma: ‘Este
primeiro jhana é fabricado pela mente e produzido pela volição. [<a href="#N2">2</a>] <a
