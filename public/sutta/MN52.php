<p class=Normal><a name=R5></a><a name=T15></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>15. Quando isso foi dito pelo venerável Ananda,
o chefe de família Dasama de Atthakanagara disse: “Venerável Ananda, tal como
se um homem, procurando pela porta de entrada para um tesouro escondido,
encontre de repente onze portas de entrada para um tesouro escondido, assim
também, enquanto eu estava procurando por uma porta para o Imortal, eu de
repente ouvi sobre onze portas para o Imortal. [<a href="#N6">6</a>] <a
name=R6></a>Tal como se um homem tivesse uma casa com onze portas, e se aquela
casa se incendiasse, ele pudesse escapar para um lugar seguro, por qualquer uma
daquelas onze portas, assim eu também posso escapar para um lugar seguro por
qualquer uma dessas onze portas para o Imortal. Venerável senhor, certos
sectários até honorários para os seus mestres tentam conseguir; porque eu não
faço um donativo para o venerável Ananda?”<o:p></o:p></span></p>



