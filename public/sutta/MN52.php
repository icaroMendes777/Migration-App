diligente, ardente e decidido, a sua mente não libertada se torna libertada, as
suas impurezas não destruídas se tornam destruídas, e ele alcança a suprema
segurança contra o cativeiro que ele não havia alcançado antes?”[<a href="#N1">1</a>]<o:p></o:p></span></p>


<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim, chefe
