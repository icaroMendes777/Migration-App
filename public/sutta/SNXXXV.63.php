<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em Savatthi. Então, o Ven. Migajala foi até o
Abençoado e depois de cumprimentá-lo sentou a um lado e disse: “’Uma pessoa vivendo
só. Uma pessoa vivendo só,’ assim dizem. De que modo, senhor, uma pessoa vive
só e de que modo uma pessoa vive com um companheiro?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Migajala, há formas
conscientizadas através do olho que são desejáveis, agradáveis e fáceis de serem
gostadas, conectadas com o desejo sensual e que provocam a cobiça, e se um
bhikkhu nelas busca o prazer, acolhe-as e permanece atado a elas, o deleite
surge. Com o deleite, surge a paixão. Com a paixão, surgem os grilhões. Atado
pelo grilhão do deleite, Migajala, esse bhikkhu é chamado aquele que vive com
um companheiro. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Há sons conscientizados através
do ouvido … aromas conscientizados através do nariz … sabores conscientizados através da
língua … tangíveis conscientizados através do corpo ... objetos mentais conscientizados
através da mente que são desejáveis, agradáveis e fáceis de serem gostados,
conectados com o desejo sensual e que provocam a cobiça e se um bhikkhu neles
busca o prazer, acolhe-os e permanece atado a eles, o deleite surge. Com o
deleite, surge a paixão. Com a paixão, surgem os grilhões. Atado pelo grilhão
do deleite, Migajala, esse bhikkhu é chamado aquele que vive com um
companheiro.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Migajala,
mesmo que um bhikkhu que viva dessa forma, freqüente locais isolados na
floresta e nos bosques, locais remotos onde há poucos sons e ruídos, deserto,
afastado das pessoas, adequado para o isolamento, ainda assim, diz-se que ele
vive com um companheiro. Por que isso? Porque a cobiça é a sua companheira e ele
não a abandonou e assim, esse bhikkhu é chamado aquele que vive com um companheiro.
<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Há,
Migajala, formas conscientizadas através do olho e que são desejáveis, agradáveis e
fáceis de serem gostadas, conectadas com o desejo sensual e que provocam a
cobiça, e se um bhikkhu não busca nelas o prazer, não as acolhe e não permanece
atado a elas, o deleite cessa. Não havendo o deleite, não há paixão. Não
havendo a paixão, não há grilhões. Libertado do grilhão do deleite, Migajala,
esse bhikkhu é chamado aquele que vive só.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Há, Migajala, sons
conscientizados através do ouvido … aromas conscientizados através do nariz … sabores
conscientizados através da língua … tangíveis conscientizados através do corpo ...
objetos mentais conscientizados através da mente que são desejáveis, agradáveis e
fáceis de serem gostados, conectados com o desejo sensual e que provocam a
cobiça, e se um bhikkhu não busca neles o prazer, não os acolhe e não permanece
atado a eles, o deleite cessa. Não havendo o deleite, não há paixão. Não
havendo a paixão, não há grilhões. Libertado do grilhão do deleite, Migajala,
esse bhikkhu é chamado aquele que vive só. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Migajala, mesmo que um
bhikkhu que viva dessa forma, viva próximo de um vilarejo, se associe com
bhikkhus e bhikkhunis, com discípulos leigos, com o rei e ministros reais, com
membros de outras seitas e seus discípulos, ainda assim, ele é chamado aquele
que vive só. Por que isso? Porque a cobiça era a sua companheira e ele a
abandonou e assim, ele é chamado aquele que vive só.” <o:p></o:p></span></p>


