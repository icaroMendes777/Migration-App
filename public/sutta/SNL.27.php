<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'> “Bhikkhus, como um pote que tenha sido virado de cabeça para baixo abandona toda a sua água e não a toma de volta, assim também um bhikkhu que desenvolve e cultiva os cinco poderes abandona todos os estados ruins e prejudiciais e não os toma de volta.[<a href="#N1">1</a>]<o:p></o:p></span></p>


<p class=Normal><a name=R1></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'> “E como um bhikkhu faz isso? Neste caso, bhikkhus, um bhikkhu desenvolve o poder da convicção, que tem como base o afastamento, desapego e cessação, que amadurece no abandono ... Ele desenvolve o poder da energia ... ele desenvolve o poder da atenção plena ... ele desenvolve o poder da concentração ... ele desenvolve o poder da sabedoria, que tem como base o afastamento, desapego e cessação, que amadurece no abandono. É dessa forma, bhikkhus, que um bhikkhu desenvolve e cultiva os cinco poderes para ele abandonar todos os estados ruins e prejudiciais e não os tomar de volta.”
