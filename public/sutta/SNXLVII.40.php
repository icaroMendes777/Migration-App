<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
eu ensinarei para vocês os fundamentos da atenção plena, o desenvolvimento dos
fundamentos da atenção plena e o caminho da prática que conduz ao
desenvolvimento dos fundamentos da atenção plena. Ouçam e prestem muita atenção
àquilo que eu vou dizer.” – “Sim, venerável senhor,” os bhikkhus responderam. O
Abençoado disse o seguinte:<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E quais, bhikkhus, são os
fundamentos da atenção plena? Neste caso, bhikkhus, um bhikkhu permanece
contemplando o corpo como um corpo, ardente, plenamente consciente e com
atenção plena, tendo colocado de lado a cobiça e o desprazer pelo mundo. Ele
permanece contemplando as sensações como sensações ... mente como mente ...
objetos mentais como objetos mentais, ardente, plenamente consciente e com
atenção plena, tendo colocado de lado a cobiça e o desprazer pelo mundo. Iisso
é chamado os fundamentos da atenção plena.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como, bhikkhus, é o
desenvolvimento dos fundamentos da atenção plena? Neste caso, bhikkhus, um
bhikkhu permanece contemplando fenômenos que surgem no corpo, ou ele permanece
contemplando fenômenos que desaparecem no corpo, ou ele permanece contemplando
ambos, fenômenos que surgem e fenômenos que desaparecem no corpo, ardente, plenamente consciente e com atenção plena, tendo
colocado de lado a cobiça e o desprazer pelo mundo. Ele permanece contemplando fenômenos que surgem nas sensações ... Ele permanece contemplando fenômenos que surgem na mente ... Ele
permanece contemplando fenômenos que surgem nos objetos mentais, ou ele permanece
contemplando fenômenos que desaparecem nos objetos mentais, ou ele permanece contemplando
ambos, fenômenos que surgem e fenômenos que desaparecem nos objetos mentais, ardente, plenamente
consciente e com atenção plena, tendo colocado de lado a cobiça e o desprazer
pelo mundo. Isso é chamado o desenvolvimento dos fundamentos da atenção plena.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual, bhikkhus, é o
caminho da prática para o desenvolvimento dos fundamentos da atenção plena?
Exatamente este nobre caminho óctuplo: entendimento correto, pensamento
correto, linguagem correta, ação correta, modo de vida correto, esforço
correto, atenção plena correta, concentração correta. Isso é chamado o caminho
da prática para o desenvolvimento dos fundamentos da atenção plena.”<a name=N6></a><a
name=N18></a><o:p></o:p></span></p>

