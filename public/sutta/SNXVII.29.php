<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, terríveis são o ganho, a honraria e fama, amargos,
vis, obstruem a realização da insuperável segurança contra o cativeiro. O
ganho, a honraria e fama cortam a pele, a camada abaixo da pele, a carne,
os tendões e depois o osso. Tendo cortado o osso, eles chegam até o tutano.
Suponham, bhikkhus, que um homem forte amarrasse a própria perna com uma corda feita
com pelos da crina de um cavalo e a puxasse com força. Ela cortaria a pele,
depois a camada abaixo da pele, a carne, os tendões e depois o osso. Tendo
cortado o osso, a corda chegaria até o tutano. Da mesma forma, bhikkhus, o
ganho, a honraria e fama cortam a pele ... eles chegam até o tutano. Tão
terríveis, bhikkhus, são o ganho, a honraria e fama, amargos, vis, obstruem
a realização da insuperável segurança contra o cativeiro. Portanto, bhikkhus,
vocês devem praticar da seguinte forma: ‘Nós abandonaremos o ganho, a honraria
e fama que já surgiram e não permitiremos que o ganho, a honraria e fama que já surgiram persistam obcecando as nossas mentes.’ Assim vocês
deveriam praticar.”<b style='mso-bidi-font-weight:normal'><o:p></o:p></b></span></p>


