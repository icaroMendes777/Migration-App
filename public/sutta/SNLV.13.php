<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em certa
ocasião, o Venerável Ananda e o Venerável Sariputta estavam em Savatthi no
Bosque de Jeta, no Parque de Anathapindika. Então, ao anoitecer o Venerável
Sariputta saiu do seu isolamento e foi até o Venerável Ananda e ambos se
cumprimentaram. Quando a conversa cortês e amigável havia terminado, ele sentou
a um lado e disse para o Venerável Ananda:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigo
Ananda, por abandonar quantas coisas e possuir quantas coisas as pessoas são
declaradas pelo Abençoado assim: ‘Ele é um que entrou na correnteza, não mais
destinado aos mundos inferiores, com o destino fixo, ele tem a iluminação como
destino’?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigo, por
abandonar quatro coisas e possuir quatro coisas é que as pessoas são declaradas
assim pelo Abençoado. Quais quatro? <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele não
possui, amigo, aquela desconfiança em relação ao Buda que a pessoa comum sem
instrução possui, devido à qual, com a dissolução do corpo, depois da morte,
ela renasce num plano de miséria, num destino ruim, nos mundos inferiores, no
inferno. E ele possui aquela perfeita claridade, serenidade e confiança em relação ao Buda que o
nobre discípulo bem instruído possui, devido à qual, com a dissolução do corpo,
depois da morte, ele renasce num bom destino, no paraíso: ‘O Abençoado é um arahant,
perfeitamente iluminado, consumado no verdadeiro conhecimento e conduta,
bem-aventurado, conhecedor dos mundos, um líder insuperável de pessoas
preparadas para serem treinadas, mestre de devas e humanos, desperto,
sublime.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele não
possui, amigo, aquela desconfiança em relação ao Dhamma que a pessoa comum sem
instrução possui, devido à qual, com a dissolução do corpo, depois da morte,
ela renasce num plano de miséria, num destino ruim, nos mundos inferiores, no
inferno. E ele possui aquela perfeita claridade, serenidade e confiança com relação ao Dhamma que o
nobre discípulo bem instruído possui, devido à qual, com a dissolução do corpo,
depois da morte, ele renasce num bom destino, no paraíso: ‘O Dhamma é bem proclamado pelo
Abençoado, visível no aqui e agora, com efeito imediato, que convida ao exame,
que conduz para adiante, para ser experimentado pelos sábios por eles mesmos.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele não
possui, amigo, aquela desconfiança em relação à Sangha que a pessoa comum sem
instrução possui, devido à qual, com a dissolução do corpo, depois da morte,
ela renasce num plano de miséria, num destino ruim, nos mundos inferiores, no
inferno. E ele possui aquela perfeita claridade, serenidade e confiança com relação à Sangha que o
nobre discípulo bem instruído possui, devido à qual, com a dissolução do corpo,
depois da morte, ele renasce num bom destino, no paraíso: ‘A Sangha dos discípulos do Abençoado
pratica o bom caminho, pratica o caminho reto, pratica o caminho verdadeiro,
pratica o caminho adequado, isto é, os quatro pares de pessoas, os oito tipos
de indivíduos; esta Sangha dos discípulos do Abençoado é merecedora de dádivas,
merecedora de hospitalidade, merecedora de oferendas, merecedora de saudações
com reverência, um campo inigualável de mérito para o mundo.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele não
possui, amigo, aquela imoralidade que a pessoa comum sem instrução possui,
devido à qual, com a dissolução do corpo, depois da morte, ela renasce num
plano de miséria, num destino ruim, nos mundos inferiores, no inferno. E ele
possui aquelas virtudes apreciadas pelos nobres que o nobre discípulo bem
instruído possui, devido à qual, com a dissolução do corpo, depois da morte,
ele renasce num bom destino, no paraíso: virtudes apreciadas pelos nobres – intactas, não-laceradas, imaculadas,
não-matizadas, libertadoras, elogiadas pelos sábios, desapegadas, que conduzem
à concentração.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigo, abandonando
quatro coisas e possuindo quatro coisas é que as pessoas são declaradas pelo
Abençoado assim: ‘Ele é um que entrou na correnteza, não mais destinado aos
mundos inferiores, com o destino fixo, ele tem a iluminação como destino’.”<o:p></o:p></span></p>



