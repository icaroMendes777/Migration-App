<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
há essas Quatro Nobres Verdades. Quais quatro? A nobre verdade do sofrimento, a
nobre verdade da origem do sofrimento, a nobre verdade da cessação do
sofrimento, a nobre verdade do caminho que conduz à cessação do sofrimento.
Essas são as Quatro Nobres Verdades.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Dessas
Quatro Nobres Verdades, bhikkhus, há uma nobre verdade que deve ser completamente
compreendida; há uma nobre verdade que deve ser abandonada; há uma nobre
verdade que deve ser realizada; há uma nobre verdade que deve ser desenvolvida.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é a nobre verdade que deve ser completamente compreendida? A nobre
verdade do sofrimento deve ser completamente compreendida; a nobre verdade da
origem do sofrimento deve ser abandonada; a nobre verdade da cessação do
sofrimento deve ser realizada; a nobre verdade do caminho que conduz à cessação
do sofrimento deve ser desenvolvida.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
bhikkhus, um esforço deve ser feito para compreender: ‘Isto é sofrimento.’ Um
esforço deve ser feito para compreender: ‘Esta é a origem do sofrimento.’ Um
esforço deve ser feito para compreender: ‘Esta é a cessação do sofrimento.’ Um
esforço deve ser feito para compreender: ‘Este é o caminho que conduz à
cessação do sofrimento.’”<o:p></o:p></span></p>


