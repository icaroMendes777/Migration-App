<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em Savathi.
”Bhikkhus, eu digo que a destruição das impurezas é realizada por aquele que
sabe e que vê, não por aquele que não sabe e que não vê. Aquele que sabe o que
e aquele que vê o que, realiza a destruição das impurezas? ‘Assim é a forma,
essa é a sua origem, essa é a sua cessação. Assim é a sensação, essa é a sua
origem, essa é a sua cessação. Assim é a percepção, essa é a sua origem, essa é
a sua cessação. Assim são as formações, essa é a sua origem, essa é a sua
cessação. Assim é a consciência, essa é a sua origem, essa é a sua cessação.’ A
destruição das impurezas é realizada por aquele que sabe dessa forma e que vê
dessa forma.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O
conhecimento da destruição das impurezas com respeito à destruição das
impurezas possui a sua condição, eu lhes digo. A esta não lhe falta uma
condição. E qual é a condição para o conhecimento da destruição
das impurezas? Libertação deveria ser a resposta. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“A
libertação possui a sua condição, eu lhes digo. A esta não lhe falta
uma condição. E qual é a sua condição? Desapego deveria ser a
resposta. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O desapego
possui a sua condição, eu lhes digo. A este não lhe falta uma condição. E qual é a sua condição? Desencantamento deveria ser a
resposta. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O
desencantamento possui a sua condição, eu lhes digo. A este não lhe
falta uma condição. E qual é a sua condição? Conhecimento e
visão das coisas como na verdade elas são deveria ser a resposta. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O
conhecimento e visão das coisas como na verdade elas são possui a sua condição, eu lhes digo. A este não lhe falta uma condição. E qual é a
sua condição? Concentração deveria ser a resposta. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“A
concentração possui a sua condição, eu lhes digo. A esta não lhe falta
uma condição. E qual é a sua condição? Felicidade deveria ser a
resposta. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“A felicidade
possui a sua condição, eu lhes digo. A esta não lhe falta uma condição. E qual é a sua condição? Calma deveria ser a resposta.
<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“A
calma possui a sua condição, eu lhes digo. A esta não lhe falta
uma condição. E qual é a sua condição? Êxtase deveria ser a
resposta. <o:p></o:p></span></p>



<p class=Normal><a name="pamujja"></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O êxtase
possui a sua condição, eu lhes digo. A este não lhe falta uma condição. E qual é a sua condição? Satisfação deveria ser a resposta. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“A
satisfação possui a sua condição, eu lhes digo. A esta não lhe falta
uma condição. E qual é a sua condição? Convicção deveria ser a
resposta. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“A
convicção possui a sua condição, eu lhes digo. A esta não lhe falta uma
condição. E qual é a sua condição? Sofrimento deveria ser a
resposta. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O
sofrimento possui a sua condição, eu lhes digo. A este não lhe falta
uma condição. E qual é a sua condição? Nascimento deveria ser a
resposta. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O
nascimento possui a sua condição, eu lhes digo. A este não lhe falta
uma condição. E qual é a sua condição? Ser/existir deveria ser
a resposta. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O
ser/existir possui a sua condição, eu lhes digo. A este não lhe falta
uma condição. E qual é a sua condição? Apego deveria ser a
resposta. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O apego
possui a sua condição, eu lhes digo. A este não lhe falta uma condição. E qual é a sua condição? Desejo deveria ser a resposta. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O desejo
possui a sua condição, eu lhes digo. A este não lhe falta uma condição. E qual é a sua condição? Sensação deveria ser a resposta. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“A sensação
possui a sua condição, eu lhes digo. A esta não lhe falta uma condição. E qual é a sua condição? Contato deveria ser a resposta. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O contato
possui a sua condição, eu lhes digo. A este não lhe falta uma condição. E qual é a sua condição? As seis bases dos sentidos deveria
ser a resposta. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“As seis
bases dos sentidos possuem a sua condição, eu lhes digo. A estas não
lhes falta uma condição. E qual é a sua condição?
Mentalidade-materialidade (nome e forma) deveria ser a resposta. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“A
mentalidade-materialidade (nome e forma) possui a sua condição, eu lhes digo. A esta
não lhe falta uma condição. E qual é a sua condição?
Consciência deveria ser a resposta. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“A
consciência possui a sua condição, eu lhes digo. A esta não lhe falta uma
condição. E qual é a sua condição? Formações volitivas deveria ser a
resposta. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“As
formações volitivas possuem a sua condição, eu lhes digo. A estas não lhes falta
uma condição. E qual é a sua condição? Ignorância deveria ser a
resposta.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Assim, as
formações volitivas possuem a ignorância como sua condição,<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>a
consciência possui as formações volitivas como sua condição, <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>a
mentalidade-materialidade (nome e forma) possui a consciência como sua condição, <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>as seis
bases dos sentidos possuem a mentalidade-materialidade (nome e forma) como sua condição, <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>o contato
possui as seis bases dos sentidos como sua condição, <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>a sensação
possui o contato como sua condição, <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>o desejo
possui a sensação como sua condição, <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>o apego
possui o desejo como sua condição, <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>o
ser/existir possui o apego como sua condição, <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>o
nascimento possui o ser/existir como sua condição, <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>o
sofrimento possui o nascimento como sua condição, <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>a convicção
possui o sofrimento como sua condição, <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>a
satisfação possui a convicção como sua condição, <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>o êxtase
possui a satisfação como sua condição, <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>a
calma possui o êxtase como sua condição, <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>a
felicidade possui a calma como sua condição, <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>a
concentração possui a felicidade como sua condição, <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>o
conhecimento e visão das coisas como na verdade elas são possui a concentração
como sua condição, <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>o
desencantamento possui o conhecimento e visão das coisas como na verdade elas
são como sua condição, <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>o desapego
possui o desencantamento como sua condição, <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>a
libertação possui o desapego como sua condição, <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>o
conhecimento da destruição das impurezas possui a libertação como sua condição, <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Tal qual
quando os devas trovejam e vertem gotas pesadas de chuva<span
style="mso-spacerun: yes">  </span>no alto das montanhas: A água flui pelas
encostas, enchendo as fissuras, rachaduras e valas. Quando as fissuras,
rachaduras e valas estão preenchidas, as gotas pesadas de chuva enchem as pequenas
lagoas. Quando as pequenas lagoas estão preenchidas, elas enchem os grandes
lagos. Quando os grandes lagos estão preenchidos, elas enchem os pequenos rios.
Quando os pequenos rios estão preenchidos, elas enchem os grandes rios. Quando
os grandes rios estão preenchidos, elas enchem o grande oceano; assim também,
tendo a ignorância como condição, as formações volitivas [surgem]; tendo as
formações volitivas como condição, a consciência; tendo a consciência
como condição, a mentalidade-materialidade (nome e forma); tendo a
mentalidade-materialidade (nome e forma) como condição, as seis bases dos sentidos;
tendo as seis bases dos sentidos como condição, o contato; tendo o
contato como condição, a sensação; tendo a sensação como condição, o desejo; tendo o desejo como condição, o apego; tendo o apego
como condição, o ser/existir; tendo o ser/existir como condição, o nascimento; tendo o nascimento como condição, o sofrimento;
tendo o sofrimento como condição, a convicção; tendo a convicção como
condição, a satisfação; tendo a satisfação como condição, o
êxtase; tendo o êxtase como condição, a calma; tendo a
calma como condição, a felicidade; tendo a felicidade como
condição, a concentração; tendo a concentração como condição, o
conhecimento e visão das coisas como na verdade elas são; tendo o conhecimento
e visão das coisas como na verdade elas são como condição, o
desencantamento; tendo o desencantamento como condição, o desapego;
tendo o desapego como condição, a libertação; tendo a libertação como
condição, o conhecimento da destruição das impurezas.”<a name=T2></a><o:p></o:p></span></p>


