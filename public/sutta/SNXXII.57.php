<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Nota de
Thanissaro Bhikkhu: </span></b><span lang=PT-BR style='mso-ansi-language:PT-BR'>o
termo “sete casos” neste sutta também pode significar as sete notas da escala
musical; e é possível que a frase “investigador tríplice” também tenha sido
emprestada da teoria musical: pode estar se referindo às três formas de testar
uma escala musical uma vez que esta esteja afinada. Assim, neste discurso, o
Buda pode estar tomando emprestado termos geralmente usados para descrever um
músico consumado, aplicando-os na descrição de um meditador consumado. O
comentário, por seu lado, seleciona este discurso como um dos que estimula um
meditador sério a praticar.<o:p></o:p></span></p>

