<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em Savathi.
“Bhikkhus, um bhikkhu que tenha habilidade em sete casos e seja um investigador
tríplice, é chamado neste Dhamma e Disciplina, aquele que é consumado, aquele
que viveu a vida santa completamente, um tipo de pessoa suprema.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como é
que um bhikkhu tem habilidade em sete casos? É o caso em que um bhikkhu
compreende a forma, a origem da forma, a cessação da forma, o caminho da
prática que conduz à cessação da forma. Ele compreende a gratificação da forma,
o perigo na forma e a escapatória da forma.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele
compreende a sensação ... Ele compreende a percepção ... Ele compreende as
formações volitivas ...<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele
compreende a consciência, a origem da consciência, a cessação da consciência, o
caminho da prática que conduz à cessação da consciência. Ele compreende a
gratificação da consciência, o perigo na consciência e a escapatória da
consciência.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que,
bhikkhus, é forma? Os quatro grandes elementos, (o elemento terra, o elemento
água, o elemento fogo e o elemento ar), e a forma deles derivada: a isso se
denomina forma. Com o surgimento do alimento comida, surge a forma. Cessando o
alimento comida, cessa a forma. E exatamente este nobre caminho óctuplo é o
caminho da prática que conduz à cessação da forma, isto é, entendimento
correto, pensamento correto, linguagem correta, ação correta, modo de vida
correto, esforço correto, atenção plena correta, concentração correta. O prazer
e a alegria que surgem na dependência da forma: essa é a gratificação da forma.
A forma é impermanente, sofrimento e sujeita à mudança: esse é o perigo na
forma. A remoção e o abandono do desejo e cobiça pela forma: essa é a
escapatória da forma.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Todos os
brâmanes ou contemplativos que, conhecendo assim, de modo direto, a forma,
conhecendo assim, de modo direto, a origem da forma, conhecendo assim, de modo
direto, a cessação da forma, conhecendo assim, de modo direto o caminho da
prática que conduz à cessação da forma, estiverem praticando para o
desencantamento, desapego, cessação em relação à forma, eles estarão praticando
do modo correto. Aqueles que praticam do modo correto estão apoiados com
firmeza neste Dhamma e Disciplina. E todos os brâmanes ou contemplativos que
assim conheceram de modo direto a forma, conheceram de modo direto a origem da
forma, conheceram de modo direto a cessação da forma, conheceram de modo direto
o caminho da prática que conduz à cessação da forma, estao, por meio do
desencantamento, desapego, cessação em relação à forma, libertados através do desapego,
eles estao bem libertados. Aqueles que estão bem libertados estão totalmente
consumados. E com relação àqueles que estão totalmente consumados, não existe
ciclo para descrevê-los.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que,
bhikkhus, é a sensação? Esses seis grupos de sensações – sensação que nasce do
contato no olho, sensação que nasce do contato no ouvido, sensação que nasce do
contato no nariz, sensação que nasce do contato na língua, sensação que nasce
do contato no corpo, sensação que nasce do contato na mente: a isso se denomina
sensação. Com o surgimento do contato, surge a sensação. Cessando o contato,
cessa a sensação. E exatamente este nobre caminho óctuplo é o caminho da
prática que conduz à cessação da sensação ... O prazer e a alegria que surgem
na dependência da sensação: essa é a gratificação da sensação. A sensação é
impermanente, sofrimento e sujeita à mudança: esse é o perigo na sensação. A
remoção e o abandono do desejo e cobiça pela sensação: essa é a escapatória da
sensação.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que,
bhikkhus, é a percepção? Esses seis grupos de percepções – percepção das formas,
percepção dos sons, percepção dos aromas, percepção dos sabores, percepção dos
tangíveis, percepção dos objetos mentais: a isso se denomina percepção. Com o
surgimento do contato, surge a percepção. Cessando o contato, cessa a
percepção. E exatamente este nobre caminho óctuplo é o caminho da prática que
conduz à cessação da percepção ... O prazer e a alegria que surgem na
dependência da percepção: essa é a gratificação da percepção. A percepção é
impermanente, sofrimento e sujeita à mudança: esse é o perigo na percepção. A
remoção e o abandono do desejo e cobiça pela percepção: essa é a escapatória da
percepção.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que,
bhikkhus, são as formações volitivas? Esses seis grupos de volições (ou intenções) – volições
relacionadas às formas, volições relacionadas aos sons, volições relacionadas aos
aromas, volições relacionadas aos sabores, volições relacionadas aos tangíveis,
volições relacionadas aos objetos mentais: a isso se denomina formações volitivas. Com o
surgimento do contato, surgem as formações volitivas. Cessando o contato, cessam as
formações volitivas. E exatamente este nobre caminho óctuplo é o caminho da prática que
conduz à cessação das formações volitivas ... O prazer e a alegria que surgem na
dependência das formações volitivas: essa é a gratificação das formações volitivas. As formações volitivas são
impermanentes, sofrimento e sujeitas à mudança: esse é o perigo nas formações volitivas.
A remoção e o abandono do desejo e cobiça pelas formações volitivas: essa é a escapatória
das formações volitivas.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que,
bhikkhus, é a consciência? Esses seis grupos de consciência - consciência no
olho, consciência no ouvido, consciência no nariz, consciência na língua,
consciência no corpo, consciência na mente: a isso se denomina consciência. Com
o surgimento da mentalidade-materialidade (nome e forma), surge a consciência. Cessando a
mentalidade-materialidade (nome e forma), cessa a consciência. E exatamente este nobre caminho
óctuplo é o caminho da prática que conduz à cessação da consciência, isto é,
entendimento correto, pensamento correto, linguagem correta, ação correta, modo
de vida correto, esforço correto, atenção plena correta, concentração
correta.&nbsp;O prazer e a alegria que surgem na dependência da consciência:
essa é a gratificação da consciência. A consciência é impermanente, sofrimento
e sujeita à mudança: esse é o perigo na consciência. A remoção e o abandono do
desejo e cobiça pela consciência: essa é a escapatória da consciência<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Todos os
brâmanes ou contemplativos que, conhecendo assim, de modo direto, a
consciência, conhecendo assim, de modo direto, a origem da consciência, conhecendo
assim, de modo direto, a cessação da consciência, conhecendo assim, de modo
direto o caminho da prática que conduz à cessação da consciência, estiverem
praticando para o desencantamento, desapego, cessação em relação à consciência,
eles estarão praticando do modo correto. Aqueles que praticam do modo correto
estão apoiados com firmeza neste Dhamma e Disciplina. E todos os brâmanes ou
contemplativos que assim conheceram de modo direto a consciência, conheceram de
modo direto a origem da consciência, conheceram de modo direto a cessação da
consciência, conheceram de modo direto o caminho da prática que conduz à
cessação da consciência, estão, por meio do desencantamento, desapego,
cessação em relação à consciência, libertados através do desapego, eles estão
bem libertados. Aqueles que estão bem libertados estão totalmente consumados. E
com relação àqueles que estão totalmente consumados, não existe ciclo para
descrevê-los.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Assim é
como um bhikkhu tem habilidade em sete casos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como um
bhikkhu é um investigador tríplice? É o caso em que o bhikkhu investiga por
meio dos elementos, investiga por meio das bases sensuais, investiga por meio
da origem dependente. Assim é como um bhikkhu é um investigador tríplice.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Um bhikkhu
que tenha habilidade nos sete casos e seja um investigador tríplice, é chamado
neste Dhamma e Disciplina, aquele que é consumado, aquele que viveu a vida
santa completamente, um tipo de pessoa suprema.”<o:p></o:p></span></p>

