nobre verdade da cessação do sofrimento foi compreendida e penetrada. Aquela
nobre verdade do caminho que conduz à cessação do sofrimento foi compreendida e
penetrada. O desejo por ser/existir foi cortado; o conduto para o ser/existir
foi destruído; agora não há mais vir a ser a nenhum estado.”<o:p></o:p></span></p>


