<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim ouvi.
Em certa ocasião, o Abençoado estava<span style="mso-spacerun: yes">  </span>em
Kosambi no Parque de <a name=PVW>Ghosita</a>. Lá ele se dirigiu aos bhikkhus desta
forma:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
existe um método através do qual um bhikkhu, que é um treinando, estando na
posição de um treinando, possa compreender: ‘Eu sou um treinando,’ enquanto que
um bhikkhu, que está além do treinamento, estando na posição de um além do
treinamento, possa compreender: ‘Eu estou além do treinamento’?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, os nossos ensinamentos têm o Abençoado como origem, como guia e como
refúgio. Seria bom se o Abençoado pudesse explicar o significado dessas
palavras. Tendo ouvido do Abençoado, os bhikkhus o recordarão.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Há um
método, bhikkhus, através do qual um bhikkhu, que é um treinando ... pode
compreender: ‘Eu sou um treinando,’ enquanto que um bhikkhu, que está além do
treinamento ... pode compreender: ‘Eu estou além do treinamento.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é o método através do qual um bhikkhu que é um treinando, estando na
posição de um treinando, compreende: ‘Eu sou um treinando’?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Neste
caso, bhikkhus, um bhikkhu que é um treinando compreende como na verdade é:
‘Isto é sofrimento’; ele compreende como na verdade é: ‘Esta é a origem do
sofrimento’; ele compreende como na verdade é: ‘Esta é a cessação do
sofrimento’; ele compreende como na verdade é: ‘Este é o caminho que conduz à
cessação do sofrimento.’ Esse é um método através do qual um bhikkhu que é um
treinando, estando na posição de um treinando, compreende: ‘Eu sou um
treinando.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Novamente,
bhikkhus, um bhikkhu que é um treinando considera assim: ‘Existe fora daqui [<a
href="#N1">1</a>] <a name=R1></a>algum outro contemplativo ou brâmane que
ensine um Dhamma tão real, verdadeiro, atual como o Abençoado ensina?’ Ele
compreende assim: ‘Não há nenhum outro contemplativo ou brâmane que ensine um
Dhamma tão real, verdadeiro, atual como o Abençoado ensina.’ Esse também é um
método através do qual um bhikkhu que é um treinando, estando na posição de um
treinando, compreende: ‘Eu sou um treinando.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Novamente,
bhikkhus, um bhikkhu que é um treinando compreende as cinco faculdades
espirituais – a faculdade da convicção, a faculdade da energia, a faculdade da
atenção plena, a faculdade da concentração, a faculdade da sabedoria. Ele ainda
não permanece tendo contatado com o corpo aquilo que é o destino delas, a
culminação delas, o fruto delas, o objetivo último delas; mas tendo penetrado
aquilo através da sabedoria, ele vê. [<a href="#N2">2</a>] <a name=R2></a>Esse
também é um método através do qual um bhikkhu que é um treinando, estando na
posição de um treinando, compreende: ‘Eu sou um treinando.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é o método através do qual um bhikkhu, que está além do treinamento,
estando na posição de um além do treinamento, possa compreender: ‘Eu estou além
do treinamento’? Neste caso, bhikkhus, um bhikkhu que está além do treinamento
compreende as cinco faculdades espirituais – a faculdade da convicção ... a
faculdade da sabedoria. Ele permanece tendo contatado com o corpo aquilo que é
o destino delas, a culminação delas, o fruto delas, o objetivo último delas; e
tendo penetrado aquilo através da sabedoria, ele vê. Esse é um método através
do qual um bhikkhu, que está além do treinamento, estando na posição de um além
do treinamento, compreende: ‘Eu estou além do treinamento.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Novamente,
bhikkhus, um bhikkhu que está além do treinamento compreende as seis faculdades
– a faculdade do olho, a faculdade do ouvido, a faculdade do nariz, a faculdade
da língua, a faculdade do corpo, a faculdade da mente. Ele compreende: ‘Essas
seis faculdades cessarão completamente e totalmente sem deixar vestígio e
nenhuma outra dessas seis faculdades surgirão em nenhum lugar de nenhuma
forma.’ Esse também é um método através do qual um bhikkhu, que está além do
treinamento, estando na posição de um além do treinamento, compreende: ‘Eu
estou além do treinamento.’<o:p></o:p></span></p>

