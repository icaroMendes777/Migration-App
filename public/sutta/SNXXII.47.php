<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em Savathi.
“Bhikkhus, aqueles contemplativos e brâmanes que fazem diversas suposições
acerca do eu, todos tomam por base os cinco agregados influenciados pelo apego
ou algum deles. Quais cinco? É o caso em que uma pessoa comum, sem instrução,
que não não respeita os nobres, que não é proficiente nem treinada no
Dhamma deles; que não respeita os homens verdadeiros, não é proficiente nem treinada 
no Dhamma deles – supõe que a forma seja o eu, ou o eu como possuído
de forma, ou a forma como estando no eu, ou o eu como estando na forma.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ela supõe
que a sensação seja o eu, ou o eu possuído de sensação, ou a sensação como
estando no eu, ou o eu como estando na sensação.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ela supõe
que a percepção seja o eu, ou o eu possuído de percepção, ou a percepção como
estando no eu, ou o eu como estando na percepção.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ela supõe
que as formações volitivas sejam o eu, ou o eu possuído de formações volitivas, ou as formações volitivas
como estando no eu, ou o eu como estando nas formações volitivas.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ela supõe
que a consciência seja o eu, ou o eu possuído de consciência, ou a consciência
como estando no eu, ou o eu como estando na consciência.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto, esse
modo de considerar as coisas e a noção ‘eu sou’ não desapareceram nela. Como
‘eu sou’ não desapareceu, ocorre o pouso das cinco faculdades – olho, ouvido,
nariz, língua e corpo. Há, bhikkhus, mente, há fenômenos mentais e o elemento
ignorância. Quando uma pessoa comum, sem instrução, tem contato com uma
sensação nascida da ignorância, ocorrem os pensamentos: ‘Eu sou,’ ‘Eu sou
assim,’ ‘Eu serei,’ ‘Eu não serei,’ ‘Eu possuirei forma,’ ‘Eu serei desprovido
de forma,’ ‘Eu serei perceptivo,’ ‘Eu não serei perceptivo,’ ‘Eu serei nem
perceptivo, nem não perceptivo.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“As cinco
faculdades, bhikkhus, ali permanecem. E com relação a elas, um discípulo dos
nobres, bem instruído, abandona a ignorância e faz surgir o verdadeiro
conhecimento. Com o desaparecimento da ignorância e o surgimento do verdadeiro
conhecimento, os pensamentos - ‘Eu sou,’ ‘Eu sou assim,’ ‘Eu serei,’ ‘Eu não
serei,’ ‘Eu possuirei forma,’ ‘Eu serei desprovido de forma,’ ‘Eu serei
perceptivo,’ ‘Eu não serei perceptivo,’ ‘Eu serei nem perceptivo, nem não
perceptivo.’ – não lhe ocorrem.”<o:p></o:p></span></p>


