<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em certa
ocasião, o Abençoado estava<span style="mso-spacerun: yes">  </span>entre os
Sakyas em Kapilavatthu, no Parque de Nigrodha. Então, o Sakya Nandiya foi até o
Abençoado e depois de cumprimentá-lo sentou a um lado e disse:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, quando os quatro fatores para entrar na correnteza estão completamente
e totalmente ausentes num nobre discípulo, então aquele nobre discípulo seria
um dos que permanece na negligência?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Nandiya,
eu digo que uma pessoa, para quem os quatro fatores daquele que entrou na
correnteza estão completamente e totalmente ausentes, é uma forasteira, alguém
que faz parte da facção dos mundanos. [<a href="#N1">1</a>] <a name=R1></a>Mas,
Nandiya, quanto a como um nobre discípulo é aquele que permanece na negligência
e aquele que permanece na diligência, ouça e preste muita atenção àquilo que
vou dizer.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
venerável senhor,” o Sakya Nandiya respondeu. O Abençoado disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como,
Nandiya, um nobre discípulo é alguém que permanece na negligência? Aqui,
Nandiya, um nobre discípulo possui perfeita claridade, serenidade e confiança no Buda assim: ‘O
Abençoado é<span style="mso-spacerun: yes">  </span>... mestre de devas e
humanos, iluminado, sublime.’ Satisfeito com essa perfeita claridade, serenidade e confiança no Buda,
ele não faz esforço adicional pela solidão durante o dia e pelo isolamento
durante a noite. Quando ele assim permanece negligente, não há satisfação. [<a
href="#N2">2</a>] <a name=R2></a>Quando não há satisfação, não há êxtase.
Quando não há êxtase, o corpo não fica calmo. Quando não há calma, ele permanece no sofrimento. 
A mente daquele que sofre não se torna concentrada. Quando a mente não está concentrada, os fenômenos não se manifestam. E porque
os fenômenos não se manifestam considera-se que ele é ‘alguém que permanece na
negligência.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Outra vez,
Nandiya, um nobre discípulo possui perfeita claridade, serenidade e confiança no Dhamma ... na Sangha
... Ele possui as virtudes apreciadas pelos nobres – intactas ... que conduzem
à concentração. Satisfeito com essas virtudes, ele não faz esforço adicional
pela solidão durante o dia e pelo isolamento durante a noite. Quando ele assim
permanece negligente, não há satisfação. Quando não há satisfação, não há
êxtase. Quando não há êxtase, o corpo não fica calmo. Quando não há calma, ele permanece no sofrimento. 
A mente daquele que sofre não se torna concentrada. Quando a mente não está concentrada, os fenômenos não se manifestam. E porque os fenômenos não se manifestam considera-se que ele é
‘alguém que permanece na negligência.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“É dessa forma,
Nandiya, que alguém permanece na negligência<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como,
Nandiya, um nobre discípulo é alguém que permanece na diligência? Aqui,
Nandiya, um nobre discípulo possui perfeita claridade, serenidade e confiança no Buda assim: ‘O
Abençoado é<span style="mso-spacerun: yes">  </span>... mestre de devas e
humanos, iluminado, sublime.’ Não satisfeito com essa perfeita claridade, serenidade e confiança no
Buda, ele faz esforço adicional pela solidão durante o dia e pelo isolamento
durante a noite. Quando ele assim permanece diligente, a satisfação surge.
Quando há satisfação, o êxtase surge. Quando a mente é alçada pelo êxtase, o corpo fica calmo. 
Com o corpo calmo, ele experimenta a felicidade. A mente daquele que sente felicidade, se torna 
concentrada. Quando a mente está concentrada, os fenômenos se manifestam. E porque os fenômenos se manifestam
considera- se que ele é ‘alguém que permanece na diligência.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Outra vez,
Nandiya, um nobre discípulo possui perfeita claridade, serenidade e confiança no Dhamma ... na Sangha
... Ele possui as virtudes apreciadas pelos nobres – intactas ... que conduzem
à concentração. Não satisfeito com essas virtudes, ele faz esforço adicional
pela solidão durante o dia e pelo isolamento durante a noite. Quando ele assim
permanece diligente, a satisfação surge. Quando há satisfação, o êxtase surge.
Quando a mente é alçada pelo êxtase, o corpo fica calmo. 
Com o corpo calmo, ele experimenta a felicidade. A mente daquele que sente felicidade, se torna 
concentrada. Quando a mente está concentrada, os fenômenos se manifestam.
E porque os fenômenos se manifestam considera- se que ele é ‘alguém que
permanece na diligência.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“É dessa
forma, Nandiya, que alguém permanece na diligência.”<o:p></o:p></span></p>




