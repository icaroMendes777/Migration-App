<p class=Normal><a name=XII></a><b><span lang=PT-BR style='mso-ansi-language:
PT-BR'>XII. Nidana-samyutta – Origem Dependente</span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O <i>Nidanasamyutta</i>
contém 93 suttas breves que tratam da origem dependente. A palavra <i>nidana</i>
significa causa ou origem e algumas vezes é empregada numa cadeia de sinônimos
que inclui <i>hetu, samudaya</i> e <i>paccaya</i>, “raiz ou causa, origem e
condição”. <i>Nidana</i> também é o nome do sutta mais longo do Cânone sobre a
origem dependente (</span><span style='font-family:'><a
href="DN15.php"><span lang=PT-BR style='mso-ansi-language:PT-BR'>DN 15</span></a></span><span
lang=PT-BR style='mso-ansi-language:PT-BR'>). A origem
dependente é um dos ensinamentos centrais no Budismo, tão importante que o </span><span
style='font-family:'><a href="MN28.php"><span lang=PT-BR
style='mso-ansi-language:PT-BR'>MN 28</span></a></span><span lang=PT-BR
style='mso-ansi-language:PT-BR'> menciona a afirmação do
Buda, “Quem vê a origem dependente vê o Dhamma; quem vê o Dhamma vê a origem
dependente.” O propósito do ensinamento da origem dependente é expor as
condições que sustentam o ciclo de renascimentos, <i>samsara</i>, e mostrar o
que deve ser feito para se obter a libertação desse ciclo. A existência dentro
do samsara é sofrimento e um cativeiro, e por esta razão, o fim do sofrimento
demanda a libertação do ciclo. Para conquistar a libertação é necessário
compreender o esquema condicional que sustenta o nosso cativeiro. É a origem
dependente que define esse esquema condicional. [</span><span style='font-family:
'><a href="#RXII"><span lang=PT-BR style='mso-ansi-language:PT-BR'>Retorna</span></a></span><span
lang=PT-BR style='mso-ansi-language:PT-BR'>]</span></p>

