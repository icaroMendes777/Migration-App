<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“A
violência gera ameaça e medo: [<a href="#N1">1</a>] <a name=R1></a>veja as
pessoas brigando. Eu relatarei como eu mesmo experimentei a consternação.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Vendo as
pessoas se debatendo como peixes numa poça d'água, competindo uma contra as
outras, fui tomado pelo medo. O mundo é todo inseguro, todas as direções estão
em comoção; desejando uma permanência para mim mesmo não encontrei nenhuma
desabitada. [<a href="#N2">2</a>] <a name=R2></a>Quando vi que a competição era
a única saída, a aversão aumentou em mim; mas então eu vi uma flecha,[<a
href="#N3">3</a>] <a name=R3></a>difícil de ser vista, plantada no coração.
Lancinado por ela, alguém corre em todas as direções, mas tendo removido a
flecha ele não corre nem afunda. [<a href="#N4">4</a>]<span
style='mso-spacerun:yes'>  </span><o:p></o:p></span></p>



<p class=Normal><a name=R4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Aqui seguem as regras de treinamento:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quaisquer
grilhões mundanos, não se deixem aprisionar por eles! Desencantem-se dos
prazeres sensuais e pratiquem para realizar Nibbana! <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Um sábio
deve ser veraz, não arrogante, não fraudulento, nem dado a difamar os outros, e
deve estar desprovido de raiva. Ele deve remover a cobiça e a avareza; ele deve
conquistar o torpor, o cansaço e a preguiça e não se entregar à negligência.
Quem está empenhado por Nibbana não deve ser arrogante. Ele não deve decair
para a mentira e nem ser seduzido pelos objetos dos sentidos. Ele deve
compreender completamente a (natureza da) presunção e abster-se da
impulsividade. Ele não deve se deliciar com aquilo que já passou, nem preferir
aquilo que é novo, [<a href="#N5">5</a>] <a name=R5></a>nem se entristecer pelo
que está desaparecendo, nem cobiçar o atrativo.<span style='mso-spacerun:yes'> 
</span><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“A cobiça,
eu digo, é uma grande torrente; é um redemoinho que suga para baixo, uma fome
constante, buscando algo para agarrar, continuamente em movimento; [<a
href="#N6">6</a>] <a name=R6></a>difícil de cruzar é o pântano do desejo
sensual. Um sábio não se desvia da verdade, um brâmane [<a href="#N7">7</a>] <a
name=R7></a>permanece sobre terra firme; renunciando a tudo, ele é deveras
chamado ‘acalmado’. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Tendo de fato
experimentado e compreendido o Dhamma ele alcançou o conhecimento supremo e
está independente. [<a href="#N8">8</a>] <a name=R8></a>Ele se comporta
corretamente no mundo e aqui não inveja ninguém. Ele abandonou os prazeres
sensuais, um apego difícil de ser transcendido no mundo, não se entristece nem
tem nenhum anseio, cruzou a torrente e está libertado. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Murche o
passado, [<a href="#N9">9</a>] <a name=R9></a>que não haja nada no futuro. [<a
href="#N10">10</a>] <a name=R10></a>Se ele não agarrar a nada no presente
estará em paz. Aquele que, com relação a todo este complexo de
mentalidade-materialidade (nome e forma) não tem a noção de ‘meu,’ e que não se angustia por
aquilo que não existe, deveras não incorre perda no mundo. Para ele não existe o
pensamento ‘isso é meu’ ou ‘isso é de outrem’ com relação a nada; sem encontrar
nada que possua, e compreendendo, ‘nada é meu,’ ele não se angustia. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não ser
insensível, não ser cobiçoso, em paz e impassível com as vicissitudes – esse é
o resultado benéfico que proclamo quando perguntado sobre aquele que não
vacila. Para aquele que não deseja, que compreende, não há geração (de novo
kamma). Abstendo-se de iniciar (novo kamma) ele vê segurança em todos os lados.
Um sábio não fala em termos de ser igual, inferior ou superior. Calmo e sem
egoísmo ele nem agarra tampouco rejeita.” <o:p></o:p></span></p>

