<p class=Normal> Em certa ocasião o Abençoado estava entre os Bhaggas em Surilsumaragira no Bosque de Bhesakala, no Parque do Gamo. Então, ao amanhecer, o Abençoado se vestiu e tomando a tigela e o manto externo, foi para a residência do chefe de família Nakulapita, onde ele sentou num assento que havia sido preparado. Então o chefe de família Nakulapita e a dona de casa Nakulamata foram até o Abençoado e depois de cumprimentá-lo sentaram a um lado. O chefe de família Nakulapita então disse para o Abençoado: </p>

<p class=Normal> "Venerável senhor, desde jovem, quando a jovem moça Nakulamata me foi dada em casamento, eu não me recordo ter sido desonesto nem mesmo em pensamento, muito menos em ações. Nós desejamos, venerável senhor, encontrar-nos não somente nesta vida mas também em vidas futuras."</p>

<p class=Normal> A dona de casa Nakulamata por seu lado disse para o Abençoado: "Venerável senhor, desde quando eu era uma moça jovem dada em casamento para o jovem chefe de família Nakulapita, eu não me recordo ter sido desonesta nem mesmo em pensamento, muito menos em ações. Nós desejamos, venerável senhor, encontrar-nos não somente nesta vida mas também em vidas futuras." </p>

<p class=Normal> "Chefes de família, se ambos o esposo e a esposa desejarem encontrar-se não somente nesta vida mas também em vidas futuras, eles devem possuir a mesma fé, o mesmo comportamento virtuoso, a mesma generosidade, e a mesma sabedoria. Assim eles encontrarão um ao outro não somente nesta vida mas também em vidas futuras." </p>

<p class=Normal> Ambos esposo e esposa dotados de fé,<br>
benévolos e com autocontrole, <br>
vivendo a vida com integridade, <br>
dirigindo-se um ao outro com palavras gentis. </p>

<p class=Normal> Assim muitos méritos são acumulados<br>
e eles permanecem tranquilos. <br>
Os seus inimigos se entristecem<br>
quando ambos possuem a mesma virtude. </p>

<p class=Normal> Tendo praticado o Dhamma aqui, <br>
iguais no comportamento virtuoso e práticas, <br>
deliciando-se [após a morte] num mundo dos devas, <br>
eles se alegram, desfrutando dos prazeres dos sentidos.</p>

