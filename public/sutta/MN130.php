<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.
“Bhikkhus, suponham que </span>houvesse duas casas com portas e um homem com
boa visão parado entre elas visse as pessoas entrando nas casas e saindo, indo
e vindo<span lang=PT-BR style='mso-ansi-language:PT-BR'>. </span>Da mesma forma<span
