purificado e sobrepuja o humano, eu vi seres falecendo e renascendo, inferiores
e superiores, bonitos e feios, afortunados e desafortunados. Eu compreendi como
os seres prosseguem de acordo com as suas ações desta forma: ‘Esses seres – que
eram dotados de boa conduta com o corpo, linguagem e mente, que não insultaram
os nobres, com o<span style="mso-spacerun: yes">  </span>entendimento correto e
que realizaram ações sob a influência do entendimento correto – com a
dissolução do corpo, após a morte, renasceram num bom destino, no paraíso. Ou
esses seres – que eram dotados de boa conduta com o corpo, linguagem e mente,
que não insultaram os nobres, com o<span style="mso-spacerun: yes"> 
</span>entendimento correto e que realizaram ações sob a influência do
entendimento correto – com a dissolução do corpo, após a morte, renasceram
entre os seres humanos. Mas esses seres – que eram dotados de má conduta com o
corpo, linguagem e mente, que insultaram os nobres, com o entendimento
incorreto e que realizaram ações sob a influência do entendimento incorreto –
com a dissolução do corpo, após a morte, renasceram no reino dos fantasmas. Ou
esses seres – que eram dotados de má conduta ... com a dissolução do corpo, após
a morte, renasceram no mundo animal. Ou esses seres – que eram dotados de má
conduta ... com a dissolução do corpo, após a morte, renasceram num estado de
privação, num destino infeliz, nos reinos inferiores, até mesmo no <span
style='mso-bidi-font-weight:bold'>inferno.’</span><o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. Agora os
