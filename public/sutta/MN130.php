o Rei Yama diz: ‘Bom homem, devido à negligência você não fez o bem através do
corpo, linguagem e mente. Com certeza, eles irão tratá-lo de acordo com a sua
negligência. Mas essa sua ação prejudicial não foi cometida pela sua mãe ou seu pai,
ou seu irmão, ou sua irmã, ou pelos seus amigos e companheiros, ou pelos seus
pares e parentes, ou pelos contemplativos e brâmanes, ou pelos devas: essa
ação prejudicial foi cometida por você mesmo, e você mesmo experimentará o resultado
dela.’<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. “Então, depois
