<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, vocês deveriam se aproximar das famílias como a lua –
retraindo o corpo e a mente, comportando-se sempre como recém-chegado, pudico com as famílias. [<a href="#N1">1</a>] <a name=R1></a>Como um
homem que ao olhar para dentro de um poço velho, um precipício ou uma íngreme
ribanceira, retrairia o corpo e a mente, da mesma forma, bhikkhus, vocês deveriam
se aproximar das famílias.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
Kassapa se aproxima das famílias como a lua - retraindo o corpo e a mente,
sempre se comportando como um recém-chegado, pudico com as
famílias. O que vocês pensam, bhikkhus, que tipo de bhikkhu é digno de se
aproximar das famílias?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, os nossos ensinamentos têm o Abençoado como origem, como guia e como
refúgio. Seria bom se o Abençoado pudesse explicar o significado dessas
palavras. Tendo ouvido do Abençoado, os bhikkhus o recordarão.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o
Abençoado moveu a mão no espaço e disse: “Bhikkhus, assim como esta mão não é
capturada pelo espaço, não é aprisionada por ele, não é atada por ele, da mesma
forma quando um bhikkhu se aproxima das famílias a sua mente não é capturada, não
é aprisionada e atada entre as famílias, pensando: ‘Que aqueles que desejem
ganhos obtenham ganhos, que aqueles que desejem méritos realizem méritos!’ Ele
fica feliz e contente com os ganhos dos outros, do mesmo modo que com os seus
ganhos. Esse bhikkhu é digno de se aproximar de famílias. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
quando Kassapa se aproxima de famílias a mente dele não é capturada, não é
aprisionada e atada entre as famílias, pensando: ‘Que aqueles que desejem
ganhos obtenham ganhos, que aqueles que desejem méritos realizem méritos!’ Ele
fica feliz e contente com os ganhos dos outros, do mesmo modo que com os seus
ganhos. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O que
vocês pensam, bhikkhus, como o ensinamento do Dhamma por um bhikkhu é impuro, e
como o ensinamento do Dhamma é puro?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, os nossos ensinamentos têm o Abençoado como origem ... ”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então
ouçam e prestem muita atenção àquilo que eu vou dizer.” – “Sim, venerável
senhor,” os bhikkhus responderam. O Abençoado disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Um bhikkhu
ensina o Dhamma aos outros com o pensamento: ‘Oh, que eles ouçam o Dhamma de
mim! Tendo ouvido, que eles ganhem confiança no Dhamma! Tendo confiança, que
eles demonstrem essa confiança para mim!’ O ensinamento do Dhamma por esse
bhikkhu é impuro.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas um
bhikkhu ensina o Dhamma aos outros com o pensamento: ‘O Dhamma é bem proclamado
pelo Abençoado, visível no aqui e agora, com efeito imediato, que convida ao
exame, que conduz para adiante, para ser experimentado pelos sábios por eles
mesmos. Oh, que eles ouçam o Dhamma de mim! Tendo ouvido, que eles compreendam
o Dhamma! Tendo compreendido, que eles pratiquem do modo apropriado!’ Assim ele
ensina o Dhamma aos outros devido à excelência intrínseca do Dhamma; ele ensina
o Dhamma aos outros por compaixão e empatia, com compadecimento afetuoso. O
ensinamento do Dhamma por esse bhikkhu é puro.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
Kassapa ensina o Dhamma aos outros com o pensamento: ‘O Dhamma é bem proclamado
pelo Abençoado ...<span style="mso-spacerun: yes">  </span>Oh, que eles ouçam o
Dhamma ensinado por mim! Ao ouví-lo, que eles compreendam o Dhamma! Tendo
compreendido, que eles pratiquem do modo apropriado!’ Assim, ele ensina o
Dhamma aos outros devido à excelência intrínseca do Dhamma; ele ensina o Dhamma
aos outros por compaixão e empatia, com compadecimento afetuoso.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
eu os exortarei com o exemplo de Kassapa ou alguém semelhante a Kassapa. Sendo
exortados, vocês assim deveriam praticar.”<o:p></o:p></span></p>


