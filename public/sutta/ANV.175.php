<p class=Normal> “Dotado com essas cinco qualidades, um discípulo leigo é um pária
dentre os discípulos leigos, uma mácula dentre os discípulos leigos, a escória
dentre os discípulos leigos. Quais cinco? Ele não possui convicção [na
iluminação do Buda]; ele não possui virtude; ele é ávido por amuletos e
cerimonias de proteção; ele confia nos amuletos e cerimonias de proteção, não
no kamma; ele busca receptores de suas oferendas fora da Sangha e os favorece
primeiro. Dotado com essas cinco qualidades, um discípulo leigo é um pária
dentre os discípulos leigos, uma mácula dentre os discípulos leigos, a escória
dentre os discípulos leigos.<o:p></o:p></span></p>



<p class=Normal> “Dotado com essas cinco qualidades, um discípulo leigo é uma jóia
dentre os discípulos leigos, uma flor de lótus dentre os discípulos leigos, uma
fina flor dentre os discípulos leigos. Quais cinco? Ele possui convicção; ele é
virtuoso; ele não é ávido por amuletos e cerimonias de proteção; ele confia no
kamma e não em amuletos e cerimonias de proteção; ele não busca receptores para
suas oferendas fora da Sangha e favorece a Sangha primeiro. Dotado com essas
cinco qualidades, um discípulo leigo é uma jóia dentre os discípulos leigos,
uma flor de lótus dentre os discípulos leigos, uma fina flor dentre os
discípulos leigos.”<o:p></o:p></span></p>




