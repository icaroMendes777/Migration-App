<p class=Normal> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: "Bhikkhus, há esses três fogos. O fogo da cobiça, o fogo da raiva, e o fogo da delusão. Esses, bhikkhu, são os três fogos."
</p>

<p class=Normal style='margin-left:.75in'> O fogo da cobiça incendeia os mortais<br>
apaixonados pelos prazeres sensuais; <br>
o fogo da raiva incendeia as pessoas malévolas<br>
que matam outros seres vivos;</p>

<p class=Normal style='margin-left:.75in'> O fogo da delusão incendeia os confusos<br>
que desconhecem o Dhamma dos nobres. <br>
Sem ter ciência desses fogos, <br>
a humanidade se delicia com a identidade.</p>

<p class=Normal style='margin-left:.75in'>Sem estar liberto dos grilhões de Mara<br>
eles incham a população do inferno, <br>
os seres no reino animal, <br>
os demônios do Asura e o mundo dos fantasmas.</p>



