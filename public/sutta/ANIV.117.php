<p class=Normal> “Bhikkhus, alguém empenhado pelo seu próprio bem-estar deveria praticar a diligência, a atenção plena, e proteger a mente em quatro situações. Quais quatro? </p>

<p class=Normal> (1) "'Que a minha mente não fique excitada por coisas que provocam a cobiça!' Alguém empenhado pelo seu próprio bem-estar deveria praticar a diligência, a atenção plena, e proteger a mente assim. </p>

<p class=Normal> (2) "'Que a minha mente não seja tomada pela raiva por coisas que provocam a raiva!' Alguém empenhado pelo seu próprio bem-estar deveria praticar a diligência, a atenção plena, e proteger a mente assim. </p>

<p class=Normal> (3) "'Que a minha mente não seja deludida por coisas que causam delusão!' Alguém empenhado pelo seu próprio bem-estar deveria praticar a diligência, a atenção plena, e proteger a mente assim. </p>

<p class=Normal> (4) "'Que a minha mente não fique embriagada por coisas que provocam a embriaguez!' Alguém empenhado pelo seu próprio bem-estar deveria praticar a diligência, a atenção plena, e proteger a mente assim. </p>

<p class=Normal>"Bhikkhus, quando a mente de um bhikkhu não é excitada por coisas que provocam a cobiça é porque ele se livrou da cobiça; quando a sua mente não está tomada pela raiva por coisas que provocam a raiva é porque ele se livrou da raiva; quando a sua mente não está deludida por coisas que causam a delusão é porque ele se livrou da delusão; quando a sua mente não fica embriagada por coisas que provocam a embriaguez é porque ele se livrou da embriaguez, então, ele não se acovarda, não se agita, não treme ou fica atemorizado, nem é influenciado pelas palavras de outros contemplativos." </p>

