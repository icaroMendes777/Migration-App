<p class=Normal> “Bhikkhus, eu lhes ensinarei o Dhamma para a completa compreensão de todo o apego. Ouçam e prestem muita atenção àquilo que eu vou dizer.” – “Sim, venerável senhor,” os bhikkhus responderam. O Abençoado disse o seguinte:</p>

<p class=Normal>"Qual, bhikkhus, é o Dhamma para a completa compreensão de todo o apego? Na dependência do olho e das formas surge a consciência no olho. O encontro dos três é o contato. Com o contato como condição, a sensação surge. Vendo isso, o nobre discípulo bem instruído experimenta o desencantamento  em relação ao olho, em relação às formas, em relação à consciência no olho, em relação ao contato no olho, em relação à sensação. Experimentando o desencantamento ele se torna desapegado. Através do desapego a sua mente está libertada. Com a libertação ele compreende: 'O apego foi completamente compreendido por mim.'</p>

<p class=Normal>Na dependência do ouvido e dos sons ... na dependência da mente e dos objetos mentais surge a consciência na mente. O encontro dos três é o contato. Com o contato como condição, a sensação surge. Vendo isso, o nobre discípulo bem instruído experimenta o desencantamento  em relação à mente, em relação aos objetos mentais, em relação à consciência na mente, em relação ao contato na mente, em relação à sensação. Experimentando o desencantamento ele se torna desapegado. Através do desapego a sua mente está libertada. Com a libertação ele compreende: 'O apego foi completamente compreendido por mim.'</p>

<p class=Normal>"Esse bhikkhus, é o Dhamma para a completa compreensão de todo o apego."</p>

