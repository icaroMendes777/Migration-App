<p class=Normal><b> Notas:<o:p></o:p></span></b></p>

<p class=Normal><a
name=N1></a> [1] A mente superior, (<i>adhicitta</i>),<b> </b>é a mente com os 8 predicados adquiridos na
meditação, (<i>jhanas</i>), e que são a base para o desenvolvimento do insight;
tem a denominação de “superior“ porque está num plano superior em relação
à<span style='mso-spacerun:yes'>  </span>mente sadia comum. Os cinco “sinais”, (<i>nimittta</i>),
podem ser entendidos como métodos práticos para a remoção dos pensamentos que
distraem. Eles devem ser utilizados apenas quando as distrações se tornarem
persistentes e intrusas; em outras ocasiões o meditador deve permanecer com o
seu sinal de meditação principal. [<a href="#R1">Retorna</a>]<b><o:p></o:p></b></span></p>


<p class=Normal><a name=N2></a> [2] Quando surgirem
pensamentos<span class=GramE><span style='mso-spacerun:yes'>  </span></span>sensuais
em relação a outros seres vivos, o “outro sinal” é a atenção para a repulsa (veja <a href="MN10.php#T10">MN 10.10</a>); quando os pensamentos
forem dirigidos a coisas inanimadas, o “outro sinal” é<span
style='mso-spacerun:yes'>  </span>a atenção sobre a impermanência. Quando
surgirem pensamentos de raiva em relação a outros seres vivos, o “outro sinal”
é a meditação sobre o amor bondade, (<i>metta<span class=GramE> <span
style='font-style:normal'>)</span></span></i>; quando forem dirigidos a objetos
inanimados, o “outro sinal” é a atenção sobre os elementos (veja <a
href="MN10.php#T12">MN 10.12</a>). O remédio para pensamentos correlacionados
com a delusão é receber a orientação de um mestre, estudar o Dhamma, investigar
o seu significado, escutar o Dhamma, investigar as suas causas. [<a href="#R2">Retorna</a>]<b><o:p></o:p></b></span></p>


<p class=Normal><a name=N3></a> [3] Este método está
ilustrado pela reflexão do Bodisatva contida no <a href="MN19.php">MN 19</a>.
Trazendo para a mente o ultraje em relação aos pensamentos prejudiciais produz
um sentimento de vergonha de cometer transgressões, (<i>hiri</i>); trazer para
a mente as suas perigosas conseqüências estimula o temor de cometer
transgressões, (<i>ottappa</i>). [<a href="#R3">Retorna</a>]<b><o:p></o:p></b></span></p>


<p class=Normal><a name=N4></a> [4] <i>Vitakka-sankhara-santhanam.</span></i>  Neste caso <i>sankhara
</i>significa condição, causa ou raiz e o composto significa “parando a causa
do pensamento.” Isto se alcança pela investigação;<span
style='mso-spacerun:yes'>  </span>quando um pensamento prejudicial surge: “Qual
é a sua causa? Qual é a causa da causa?” etc.. Esse tipo de investigação irá
reduzir a intensidade e por fim cessar por completo o fluxo de pensamentos
prejudiciais. [<a href="#R4">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N5></a> [5] Ele deve subjugar o estado
mental prejudicial com um estado mental benéfico. [<a href="#R5">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N6></a> [6] A presunção num nível mais
sutil é a presunção de que “eu sou”, que permanece no contínuo da mente até
atingir o estado de arahant. A “penetração da presunção”, (<i style='mso-bidi-font-style:
normal'>manabhisamaya</i>), significa ver a presunção por dentro e abandoná-la,
e ambos são alcançados com o caminho do arahant. O bhikkhu deu “um fim ao
sofrimento” no sentido de que ele deu fim ao sofrimento do ciclo do samsara, (<i
style='mso-bidi-font-style:normal'>vattadukkha</i>). [<a href="#R6">Retorna</a>]<o:p></o:p></span></p>


