<p class=Normal> (118) "Bhikkhus, para o conhecimento direto da cobiça, oito coisas devem ser desenvolvidas. Quais oito? Entendimento correto, pensamento correto, linguagem correta, ação correta, modo de vida correto, esforço correto, atenção plena correta, concentração correta. Para o conhecimento direto da cobiça, essas oito coisas devem ser desenvolvidas." </p>

<p class=Normal> (119) "Bhikkhus, para o conhecimento direto da cobiça, oito coisas devem ser desenvolvidas. Quais oito? (1) Percebendo a forma no interior, ele vê a forma no exterior, limitada, bonita e feia; ao transcendê-la, ele percebe assim: ‘Eu sei, eu vejo.’ (2) Percebendo a forma no interior, ele vê a forma no exterior, imensurável, bonita e feia; ao transcendê-la, ele percebe assim: ‘Eu sei, eu vejo.’ Essa é a segunda base para a transcendência. (3) Não percebendo a forma no interior, ele vê a forma no exterior, limitada, bonita e feia; ao transcendê-la, ele percebe assim: ‘Eu sei, eu vejo.’ (4) Não percebendo a forma no interior, ele vê a forma no exterior, imensurável, bonita e feia; ao transcendê-la, ele percebe assim: ‘Eu sei, eu vejo.’ (5) Não percebendo a forma no interior, ele vê a forma no exterior, azul, de cor azul, azul na aparência, com luminosidade azul ... (6) ... amarela, de cor amarela, amarela na aparência, com luminosidade amarela ... (7) ... vermelha, de cor vermelha, vermelha na aparência, com luminosidade vermelha ... (8) ... branca, de cor branca, branca na aparência, com luminosidade branca; ao transcendê-la, ele percebe assim: ‘Eu sei, eu vejo.’ Para o conhecimento direto da cobiça, essas oito coisas devem ser desenvolvidas." </p>

<p class=Normal> (120) "Bhikkhus, para o conhecimento direto da cobiça, oito coisas devem ser desenvolvidas. Quais oito? (1) Possuindo forma material, ele vê a forma. (2) Não percebendo a forma no interior, ele vê a forma no exterior. (3) Ele está decidido apenas pelo belo. (4) Com a completa superação das percepções da forma, com o desaparecimento das percepções do contato sensorial, sem dar atenção às percepções da diversidade, consciente que o ‘espaço é infinito,’ ele entra e permanece na base do espaço infinito. (5) Com a completa superação da base do espaço infinito, consciente de que a ‘consciência é infinita,' ele entra e permanece na base da consciência infinita. (6) Com a completa superação da base da consciência infinita, consciente de que ‘não há nada,’ ele entra e permanece na base do nada. (7) Com a completa superação da base do nada, ele entra e permanece na base da nem percepção, nem não percepção. (8) Com a completa superação da base da nem percepção, nem não percepção, ele entra e permanece na cessação da sensação e percepção Para o conhecimento direto da cobiça, essas oito coisas devem ser desenvolvidas." </p>

<p class=Normal> (121 - 147) "Bhikkhus, para a completa compreensão da cobiça ... para a completa destruição ... para o abandono ... para a destruição ... para o desaparecimento ... para o desapego ... para a cessação .... para abrir mão ... para a renúncia da cobiça, essas oito coisas devem ser desenvolvidas." </p>

<p class=Normal> (148 - 627) "Bhikkhus, para o conhecimento direto ... para a completa compreensão .... para a completa destruição ... para o abandono ... para a destruição ... para o desaparecimento ... para o desapego ... para a cessação .... para abrir mão ... para a renúncia da raiva ... da delusão ... do ódio ... da hostilidade ... da difamação ... da insolência ... da inveja ... da avareza ... da enganação ... da astúcia ... da teimosia .... da veemência ... da presunção ... da arrogância ... da embriaguez .... da negligência, essas oito coisas devem ser desenvolvidas." </p>


