<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Bhikkhus, certa ocasião eu estava em Uruvela às margens do rio
Neranjara ao pé de uma figueira-dos-pagodes, pouco tempo depois de ter alcançado
a perfeita iluminação. Então, enquanto eu estava só em isolamento, um
pensamento surgiu na minha mente: ‘Este é o caminho direto para a purificação
dos seres, para superar a tristeza e a lamentação, para o desaparecimento da
dor e da angústia, para alcançar o caminho verdadeiro, para a realização de
Nibbana – isto é, os quatro fundamentos da atenção plena. Quais são os quatro?
Aqui, um bhikkhu permanece contemplando o corpo como um corpo, ardente,
plenamente consciente e com atenção plena, tendo colocado de lado a cobiça e o
desprazer pelo mundo. Ele permanece contemplando as sensações como sensações
... a mente como mente ... os objetos mentais como objetos mentais, ardente,
plenamente consciente e com atenção plena, tendo colocado de lado a cobiça e o
desprazer pelo mundo. Esse é o caminho direto para a purificação dos seres ...
isto é, os quatro fundamentos da atenção plena.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
bhikkhus, o Brahma <a name=PVW>Sahampati</a>, tendo percebido com a sua<span
style="mso-spacerun: yes">  </span>mente o pensamento na minha mente – com a
mesma rapidez com que um homem forte pode estender o seu braço flexionado ou
flexionar o seu braço estendido – desapareceu do mundo de Brahma e reapareceu
na minha frente. Arrumando o seu manto externo sobre o ombro, ele juntou as
mãos numa reverenciosa saudação dizendo: ‘Assim é, Abençoado! Assim é,<span
style="mso-spacerun: yes">  </span>Iluminado! Venerável senhor, este é o caminho
direto para a purificação dos seres ... (igual acima)<span style="mso-spacerun:
yes">  </span>... isto é, os quatro fundamentos da atenção plena.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o
