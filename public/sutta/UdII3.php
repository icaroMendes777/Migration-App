<p class=Normal> Assim, ouvi. Certa ocasião, o Abençoado estava em Savatthi, no Bosque de Jeta, no Parque de Anathapindika. Agora, naquela ocasião, entre Savatthi e o Bosque de Jeta um grupo de meninos estavam batendo numa cobra com um pedaço de pau. Então, ao amanhecer, o Abençoado se vestiu e tomando a tigela e o manto externo, estava indo para Savatthi para esmolar alimentos quando ele viu aquele grupo de meninos batendo na cobra com um pedaço de pau. </p>

<p class=Normal>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p>

<p class=Normal style='margin-left:.75in'> Quem machuca com um pedaço de pau seres que desejam a felicidade, <br>
