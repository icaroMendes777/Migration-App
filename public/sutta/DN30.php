<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.10.
“Bhikkhus, em qualquer vida passada ... o Tathagata tendo nascido como um ser
humano, abandonou tirar a vida de outros seres, se absteve de tirar a vida de
outros seres; permaneceu com a sua vara e arma postas de lado, bondoso e
gentil, compassivo com todos os seres vivos, por ter realizado esse kamma, ...
ele renasceu num destino feliz, no paraíso ... Falecendo ali e renascendo aqui
no mundo humano, ele adquiriu estas três marcas de um Grande Homem: (3) Ele tem
os calcanhares protuberantes, (4) Os dedos dos pés e das mãos são longos, e
(15) Ele tem os membros retos de um Brahma.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.11.
