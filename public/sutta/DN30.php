<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.7.
“Bhikkhus, em qualquer vida passada ... o Tathagata, ao nascer como um ser
humano, viveu pela felicidade de muitos, um dissipador do medo e terror,
proporcionando abrigo e proteção de acordo com a lei, atendendo todas as
necessidades e por ter realizado esse kamma, ... ele renasceu num destino
feliz, no paraíso ... Falecendo ali e renascendo aqui no mundo humano, ele
adquiriu esta marca de um Grande Homem: (2) Nas solas dos seus pés há rodas com
mil raios e cubo, toda completa.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.8. “Sendo
