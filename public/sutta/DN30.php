<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.1. Assim
ouvi. Em certa ocasião, o Abençoado estava<span style='mso-spacerun:yes'> 
</span>em Savatthi no Bosque de Jeta, no Parque de Anathapindika. “Bhikkhus!”
ele disse, e os monges responderam: “Venerável Senhor.” O Abençoado disse:
“Bhikkhus, há essas trinta e duas marcas peculiares a um Grande Homem, [<a
href="#N1">1</a>] <a name=R1></a>e o grande homem que as possui tem apenas dois possíveis destinos, nenhum outro. 
Se ele viver a vida em família, ele se tornará um Monarca que gira a roda, um monarca
justo que governará de acordo com o Dhamma; conquistador dos quatro pontos
cardeais, que estabelecerá a segurança no seu reino e que possuirá os sete
tesouros. Que são: a Roda Preciosa, o Elefante Precioso, o Cavalo Precioso, a
Jóia Preciosa, a Mulher Preciosa, o Tesoureiro Precioso e como sétimo o
Conselheiro Precioso. Ele terá mais de mil filhos, que serão corajosos e
heróicos e que aniquilarão os exércitos inimigos. Ele governará, tendo
conquistado esta terra circundada pelo mar, sem bastão ou espada, através do
Dhamma. Mas se ele deixar a vida em família e seguir a vida santa, então ele se
tornará um arahant, um Buda perfeitamente iluminado, aquele que remove o véu do
mundo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.2. “E
quais são essas trinta e duas marcas?<span style='mso-spacerun:yes'> 
</span>(1) Ele tem os pés chatos. (2) Nas solas dos seus pés há rodas com
mil raios e cubo, toda completa. (3) Ele tem os calcanhares protuberantes. (4)
Os dedos dos pés e das mãos são longos. [<a href="#N2">2</a>] <a name=R2></a>(5)
As mãos e os pés são suaves e delicados. (6) As mãos e os pés são reticulados.
(7) Os seus tornozelos são alongados. (8) As suas pernas são como as de um
antílope. (9) Quando ele fica em pé sem se inclinar, as palmas de ambas mãos
tocam os joelhos. (10) A sua genitália está contida numa bainha. (11) A sua
complexão é brilhante, de cor dourada. (12) A sua pele é sutil e devido à
sutileza da sua pele, a poeira e a sujeira não aderem ao seu corpo. (13) Os
pelos do corpo crescem separadamente, cada um no seu poro. (14) As pontas dos
pelos do corpo são curvadas para cima; a cor dos pelos é preta azulada,
enrolados para a direita. (15) Ele tem os membros retos de um Brahma. (16) Ele
tem sete convexidades. (17) Ele tem o torso de um leão. (18) Não há um sulco
entre os ombros. (19) Ele tem as proporções de uma figueira-de-bengala; a
envergadura dos seus braços equivale à altura do seu corpo, e a altura do seu
corpo equivale à envergadura dos seus braços. (20) O seu pescoço e ombros são
nivelados. (21) O seu paladar é excepcionalmente apurado. (22) Ele tem a
mandíbula de um leão. (23) Ele tem quarenta dentes. (24) Os seus dentes são nivelados.
(25) Os seus dentes não têm espaços. (26) Os seus dentes são bem brancos. (27)
Ele tem uma língua muito grande. (28) Ele tem uma voz divina, como o gorjeio de
um pássaro. (29) Os seus olhos são de um azul profundo. (30) Ele tem os cílios
de um touro. (31) Ele tem pelos no espaço entre as sobrancelhas e eles são
brancos com o brilho do algodão macio. (32) A sua cabeça tem o formato de um
turbante. [<a href="#N3">3</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>1.3. “Essas, bhikkhus, são as trinta e duas marcas peculiares a um
Grande Homem e o grande homem que as possui tem apenas dois possíveis destinos, nenhum outro ... E os
sábios de outras seitas conhecem essas trinta e duas marcas, mas eles não sabem
qual é o kamma para obtê-las. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.4.
“Bhikkhus, em qualquer vida passada, existência passada, em que o Tathagata
tenha nascido como um ser humano, ele realizou ações importantes com bons
propósitos, inabalável na conduta hábil com o corpo, linguagem e mente,
generoso, virtuoso, observando o uposatha, honrando os pais, contemplativos,
Brâmanes e o cabeça do clã; ele praticou outras ações meritórias; por ter
realizado esse kamma, acumulando-o em abundância e profusão, com a dissolução
do corpo, após a morte, ele renasceu num destino feliz, no paraíso, onde, em
relação aos outros devas, os seus predicados eram superiores em dez aspectos:
tempo de vida no paraíso, beleza, felicidade, esplendor, influência, nas suas
visões, sons, aromas, sabores e contatos paradisíacos. Falecendo ali e
renascendo aqui no mundo humano, ele adquiriu esta marca de um Grande Homem:
(1) Ele tem os pés chatos, de maneira que ele coloca o pé por igual no chão,
levanta o pé por igual e toca o chão com toda a sola do pé por igual.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.5. “Sendo
dotado com essa marca, se viver a vida em família, ele se tornará um Monarca que gira a roda,
um monarca justo que irá governar<span style='mso-spacerun:yes'>  </span>...
sem bastão ou espada, através do Dhamma. Como um Monarca que gira a roda, qual fruto ele obtém?
Ele não poderá ser impedido por nenhum inimigo com má intenção. Esse é o fruto
dele como um Monarca que gira a roda. Se ele deixar a vida em família e seguir a vida santa, ...,
então ele se tornará um Buda perfeitamente iluminado, ... Como tal, qual fruto
ele obtém? Ele não poderá ser impedido por nenhum inimigo ou adversário interno
ou externo, pela cobiça, raiva ou delusão, nem por nenhum contemplativo ou
Brâmane, nenhum deva, mara ou Brahma, ou qualquer ser no mundo. Esse é o fruto
dele como um Buda.” Isso foi o que o Abençoado declarou.<o:p></o:p></span></p>



