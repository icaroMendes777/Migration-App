<p class=Normal> “Bhikkhus, há essas cinco oferendas de uma pessoa verdadeira. [<a href="#N1">1</a>] <a name="R1"> </a>Quais cinco? Uma pessoa verdadeira dá uma oferenda com base na fé; ela dá uma oferenda respeitosamente; ela dá uma oferenda no momento apropriado; ela dá uma oferenda com o coração empático; ela dá uma oferenda sem afetar de modo adverso a si mesma ou os outros. </p>

<p class=Normal> “Porque ela dá uma oferenda com base na fé, onde quer que o fruto daquela oferenda amadureça, ela se torna rica e afluente, com muita riqueza, e ela será bela, atraente, graciosa, com a complexão de uma flor de lótus. </p> 

<p class=Normal> “Porque ela dá uma oferenda respeitosamente,  onde quer que o fruto daquela oferenda amadureça, ela se torna rica e afluente, com muita riqueza. E os seus filhos, esposas, escravos, serviçais e empregados lhe dão ouvidos com atenção, são obedientes e a atendem com o coração compreensivo. </p>

<p class=Normal> “Porque ela dá uma oferenda no momento apropriado, onde quer que o fruto daquela oferenda amadureça, ela se torna rica e afluente, com muita riqueza. E os seus objetivos são realizados no momento apropriado. </p>

<p class=Normal> “Porque ela dá uma oferenda com o coração empático, onde quer que o fruto daquela oferenda amadureça, ela se torna rica e afluente, com muita riqueza. E a sua mente se inclina pelo gozo de excelentes prazeres dos sentidos. </p>

<p class=Normal> “Porque ela dá uma oferenda sem afetar de modo adverso a si mesma ou os outros, onde quer que o fruto daquela oferenda amadureça, ela se torna rica e afluente, com muita riqueza. E de onde quer que seja não ocorre perda da sua riqueza, quer seja através do fogo, da água, de reis, de ladrões ou de parentes detestáveis. </p>

<p class=Normal> “Essas são as oferendas de uma pessoa verdadeira.” </p>

