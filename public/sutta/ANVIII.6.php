<p class=Normal> “Bhikkhus, essas oito condições do mundo mantêm o mundo girando e o mundo gira ao redor delas. Quais oito? Ganho e perda, elogio e crítica, fama e má reputação, alegria e tristeza. </p>

<p class=Normal> “Essas oito condições do mundo, bhikkhus, são encontradas por uma pessoa comum sem instrução e também são encontradas por um nobre discípulo bem instruído. Então qual é a diferença, qual é a distinção, qual é a disparidade entre o nobre discípulo bem instruído e a pessoa comum sem instrução?” </p>

<p class=Normal> “Venerável senhor, os nossos ensinamentos têm o Abençoado como origem, como guia e como refúgio. Seria bom se o Abençoado pudesse explicar o significado dessas palavras. Tendo ouvido do Abençoado, os bhikkhus o recordarão.” </p>
<p class=Normal> “Nesse  caso, bhikkhus, ouçam e prestem muita atenção àquilo que eu vou dizer.” – “Sim, venerável senhor,” os bhikkhus responderam. O Abençoado disse o seguinte: </p>
<p class=Normal> “Quando o ganho surge para uma pessoa comum sem instrução, ela não reflete deste modo: ‘Esse ganho que surgiu para mim é impermanente, atado ao sofrimento, sujeito à mudança.’ Ela não compreende isso como na verdade é. </p>

<p class=Normal> “Quando a perda … elogio … crítica ... fama ... má reputação ... alegria ... tristeza surge para uma pessoa comum sem instrução, ela não reflete deste modo: ‘Essa tristeza que surgiu para mim é impermanente, atada ao sofrimento, sujeita à mudança.’ Ela não compreende isso como na verdade é. </p>

<p class=Normal> “A mente dela permanece consumida pelo ganho e perda ... alegria e tristeza. Ela dá as boas-vindas para o ganho que surgiu e se rebela contra a perda que surgiu. Ela dá as boas-vindas para o elogio que surgiu e se rebela contra a crítica que surgiu. Ela dá as boas-vindas para a fama que surgiu e se rebela contra a má reputação que surgiu. Ela dá as boas-vindas para a alegria que surgiu e se rebela contra a tristeza que surgiu. Estando assim envolvida em gostos e desgostos, ela não está livre do nascimento, envelhecimento e morte; da tristeza, lamentação, dor, angústia e desespero. Ela não está livre, eu lhes digo, do sofrimento. </p> 

<p class=Normal> “Mas, bhikkhus, quando o ganho surge para um nobre discípulo bem instruído, ele reflete deste modo:  ‘Esse ganho que surgiu para mim é impermanente, atado ao sofrimento, sujeito à mudança.’ Ele compreende isso como na verdade é. </p>

<p class=Normal> “Quando a perda … elogio … crítica ... fama ... má reputação ... alegria ... tristeza surge para um nobre discípulo bem instruído, ele reflete deste modo: ‘Essa tristeza que surgiu para mim é impermanente, atada ao sofrimento, sujeita à mudança.’ Ele compreende isso como na verdade é. </p>

<p class=Normal> “A mente dele não permanece consumida pelo ganho e perda ... alegria e tristeza. Ele não dá as boas-vindas para o ganho que surgiu e não se rebela contra a perda que surgiu. Ele não dá as boas-vindas para o elogio que surgiu e não se rebela contra a crítica que surgiu. Ele não dá as boas-vindas para a fama que surgiu e não se rebela contra a má reputação que surgiu. Ele não dá as boas-vindas para a alegria que surgiu e não se rebela contra a tristeza que surgiu. Tendo assim abandonado os gostos e desgostos, ele está livre do nascimento, envelhecimento e morte; da tristeza, lamentação, dor, angústia e desespero. Ele está livre, eu lhes digo, do sofrimento. </p>

<p class=Normal> “Essa é a diferença, a distinção, a disparidade entre o nobre discípulo bem instruído e a pessoa comum sem instrução.” </p>

<p class=Normal style='margin-left:.75in'>Ganho e perda, elogio e crítica,<br> 
fama e má reputação, alegria e tristeza: <br>
essas são coisas transitórias na vida humana, <br>
impermanentes e sujeitas à mudança. </p>

<p class=Normal style='margin-left:.75in'>O sábio, com atenção plena, as compreende bem, <br>
observante dessas condições mutáveis. <br>
As coisas desejáveis não encantam a sua mente, <br>
as indesejáveis não ocasionam nenhuma resistência. </p>

<p class=Normal style='margin-left:.75in'>Todos os gostos e desgostos foram por ele dissipados, <br>
eliminados e abolidos. <br>
Tendo penetrado o estado imaculado, livre da tristeza, <br>
ele compreende completamente, tendo alcançado a outra margem. </p>

