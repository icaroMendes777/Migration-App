<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim ouvi.
Em certa ocasião, o Abençoado estava em Savatthi no Bosque de Jeta, no
Parque de Anathapindika. Então um certo bhikkhu foi até o Abençoado e depois de
cumprimentá-lo sentou a um lado e disse:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, seria bom se o Abençoado me ensinasse o Dhamma de forma resumida, de
modo que depois de ouvir o Dhamma do Abençoado eu possa permanecer só, isolado,
diligente, ardente e decidido.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhu,
com apego alguém está atado por Mara; sem apego ele estará libertado do Senhor
do Mal.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu
compreendo, Abençoado! Eu Compreendo, Iluminado!”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como, bhikkhu,
você compreende o significado em detalhe do que eu disse de forma resumida?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Com apego
à forma, venerável senhor, alguém está atado por Mara; sem apego à forma ele
estará libertado do Senhor do Mal. Com apego à sensação ... à percepção ... às
formações volitivas ... à consciência ... alguém está atado por Mara; sem apego à
consciência ele estará libertado do Senhor do Mal<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Senhor,
assim é como compreendo o significado em detalhe do que o Abençoado disse de
forma resumida.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Muito bem,
bhikkhu. É bom que seja assim que você tenha entendido o significado em detalhe
do que eu disse de forma resumida. Com apego à forma, alguém está atado por
Mara ... sem apego à consciência ele estará libertado do Senhor do Mal. Assim
é<span style="mso-spacerun: yes">  </span>como o significado em detalhe do que
eu disse de forma resumida deve ser entendido.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o
bhikkhu, satisfeito e contente com as palavras do Abençoado, levantou do seu
assento e depois de homenagear o Abençoado, mantendo-o à sua direita, partiu.
Depois, permanecendo só, isolado, diligente, ardente e decidido, </span>em
pouco tempo<span lang=PT-BR style='mso-ansi-language:PT-BR'>, ele alcançou e
permaneceu no objetivo supremo da vida santa, pelo qual membros de um clã
deixam a vida em família pela vida santa, </span>tendo conhecido e realizado
por si mesmo no aqui e agora<span lang=PT-BR style='mso-ansi-language:PT-BR'>.
Ele soube: “O nascimento foi destruído, a vida santa foi vivida, o que deveria
ser feito foi feito, não há mais vir a ser a nenhum estado.” E assim ele se
tornou mais um dos arahants.<b style='mso-bidi-font-weight:normal'><o:p></o:p></b></span></p>


