<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em certa
ocasião, o Venerável Sariputta estava em Savatthi no Bosque de Jeta, no Parque de
Anathapindika. Então, um certo bhikkhu foi até o Venerável Sariputta e depois
de cumprimentá-lo sentou a um lado e disse:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigo
Sariputta, um bhikkhu que era o meu companheiro abandonou o treinamento e
regressou para a vida comum.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Assim é,
amigo, quando uma pessoa não guarda as portas dos meios dos sentidos, não é
moderada na alimentação e não é dedicada à vigilância. Que um bhikkhu que não guarde
as portas das faculdades dos sentidos, que não tenha moderação na alimentação e que
não seja dedicado à vigilância possa manter durante toda a sua vida esta vida
santa completamente perfeita e imaculada – isso é impossível. Mas, amigo, que um bhikkhu que
guarde as portas das faculdades dos sentidos, que tenha moderação na alimentação e
que seja dedicado à vigilância possa manter durante toda a sua vida esta vida
santa completamente perfeita e imaculada – isso é possível. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como,
amigo, ele guarda as portas dos meios dos sentidos? Neste caso, ao ver uma
forma com o olho, um bhikkhu </span>não se agarra aos seus sinais ou detalhes.
Visto que, se permanecer com a faculdade do olho descuidada, ele será tomado
pelos estados ruins e prejudiciais de cobiça e tristeza. Ele pratica a
contenção, ele protege a faculdade do olho, ele se empenha na contenção da
faculdade do olho. Ao ouvir um som com o ouvido ... Ao cheirar um aroma com o
nariz … Ao saborear um sabor com a língua … Ao tocar um tangível com o corpo …
Ao conscientizar um objeto mental<span style="mso-spacerun: yes">  </span>com a<span
style="mso-spacerun: yes">  </span>mente, ele não se agarra aos seus sinais ou
detalhes. Visto que, se permanecer com a faculdade da mente descuidada, ele
será tomado pelos estados ruins e prejudiciais de cobiça e tristeza. Ele
pratica a contenção, ele protege a faculdade da mente, ele se empenha na
contenção da faculdade da mente<span lang=PT-BR style='mso-ansi-language:PT-BR'>.
É dessa forma, amigo, que ele guarda as portas dos meios dos sentidos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como,
amigo, ele é moderado na alimentação? Neste caso, refletindo de maneira sábia, um
bhikkhu não toma o alimento como forma de diversão ou para embriaguez, tampouco
com o objetivo de embelezamento e para ser mais atraente; mas somente com o
propósito de manter a resistência e continuidade deste corpo, como forma de dar
um fim ao desconforto e para auxiliar a vida santa, considerando: ‘Dessa forma
darei um fim às antigas sensações (de fome) sem despertar novas sensações (de
comida em excesso) e serei saudável e sem culpa e viverei em comodidade.’ É
dessa forma, amigo, que ele é moderado na alimentação.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como,
amigo, ele é dedicado à vigilância? Neste caso, durante o dia, enquanto caminha
para lá e para cá ou sentado, um bhikkhu purifica a mente dele dos estados
obstrutivos. Na primeira vigília da noite, enquanto estiver caminhando para lá
e para cá ou sentado, ele purifica a mente dele dos estados obstrutivos. Na
segunda vigília da noite ele deita para dormir, no lado direito, na postura do
leão com um pé sobre o outro, atento e plenamente consciente, após anotar na
mente o horário para levantar. Após levantar, na terceira vigília da noite,
enquanto estiver caminhando para cá e para lá ou sentado, ele purifica a mente
dele dos estados obstrutivos.’ É dessa forma, amigo, que ele é dedicado à
vigilância<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
amigo, assim é como você deve praticar: ‘Guardaremos as portas das faculdades
dos sentidos; teremos moderação na alimentação; seremos dedicados à vigilância.’
Assim, amigo, você deveria praticar.”<o:p></o:p></span></p>


