<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Notas:<o:p></o:p></span></b></p>



<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] A descrição completa da prática da atenção plena na respiração pode
ser encontrada no <a href="MN118.php#T15">MN 118.15</a>. [<a href="#R1">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[2] A cobiça representa o obstáculo do desejo sensual; o desprazer
representa o obstáculo da má vontade. Esses dois obstáculos são os dois
primeiros dentre os cinco obstáculos, que é a primeira seção na contemplação
dos objetos mentais no <a href="MN10.php#T36">MN 10.36</a>. Portanto, O Buda
diz isto indicando o início da contemplação dos objetos mentais. O “abandono”
quer dizer o conhecimento que realiza o abandono, por exemplo o abandono da
percepção da permanência ocorre através da contemplação da impermanência.
“Observar atento com equanimidade” significa observar com equanimidade o
desenvolvimento meditativo da mente sem forçá-la ou contê-la em demasia e
também considerando que ao alcançar a unicidade não há mais nada a ser feito
com relação à mente. [<a href="#R2">Retorna</a>] <o:p></o:p></span></p>



<p class=Normal><a name=N3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[3] As seis bases dos sentidos são como a encruzilhada; as contaminações que
surgem nas seis bases são como o amontoado de terra. Os quatro fundamentos da
atenção plena, ocorrendo em relação aos quatro objetos, são como as quatro
carroças ou carruagens. O “aplanar” dos estados ruins e prejudiciais é como o
aplanar do amontoado de terra pela carroça ou carruagem. [<a href="#R3">Retorna</a>]<o:p></o:p></span></p>





