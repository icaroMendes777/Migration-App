<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim ouvi.
Em certa ocasião, o Abençoado estava<span style='mso-spacerun:yes'>  </span>em Kimbila
no Bambual. Lá ele se dirigiu ao Venerável Kimbila desta forma:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Como é,
Kimbila, que a concentração através da atenção plena é desenvolvida e cultivada
para que traga grandes frutos e benefícios?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isso
foi dito, o Venerável Kimbila ficou em silêncio. Uma segunda vez ... uma
terceira vez o Abençoado se dirigiu ao Venerável Kimbila: “Como é, Kimbila, que
a concentração através da atenção plena é desenvolvida e cultivada para que
traga grandes frutos e benefícios?” Uma terceira vez o Venerável Kimbila ficou
em silêncio.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isso
aconteceu, o Venerável Ananda disse para o Abençoado: “Agora é o momento,
Abençoado! Agora é o momento, Iluminado! O Abençoado deveria explicar a
concentração através da atenção plena na respiração. Ouvindo do Abençoado os
bhikkhus o recordarão.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bem então,
Ananda, ouça e preste muita atenção àquilo que eu vou dizer.” – “Sim, venerável
senhor,” o venerável Ananda respondeu. O Abençoado disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>‘E como,
Ananda, a concentração através da atenção plena é desenvolvida e cultivada para
que traga grandes frutos e grandes benefícios? Aqui, Ananda, um bhikkhu,
dirigindo-se à floresta ou à sombra de uma árvore ou a um local isolado;
senta-se com as pernas cruzadas, mantém o corpo ereto e estabelecendo a plena
atenção à sua frente, ele inspira com atenção plena justa, ele expira com
atenção plena justa ... Ele treina dessa forma: ‘Eu inspiro contemplando a
renúncia’; ele treina dessa forma: ‘Eu expiro contemplando a renúncia.’[<a
href="#N1">1</a>]<o:p></o:p></span></p>



<p class=Normal align=center style='text-align:center'><a name=R1></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>(I. Contemplação do corpo)<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sempre,
Ananda, que um bhikkhu, inspirando longo, compreende: ‘Eu inspiro longo’; ou
expirando longo, compreende: ‘Eu expiro longo.’ Inspirando curto, ele
compreende: ‘Eu inspiro curto’; ou expirando curto, ele compreende: ‘Eu expiro
curto.’ Quando ele treina assim: ‘ Eu inspiro experienciando todo o corpo’;
quando ele treina assim: ‘Eu expiro experienciando todo o corpo.’ Quando ele
treina assim: ‘Eu inspiro tranqüilizando a formação do corpo’; quando ele
treina assim: ‘ Eu expiro tranqüilizando a formação do corpo’ – nessa ocasião,
o bhikkhu permanece contemplando o corpo como um corpo, ardente, plenamente
consciente e com atenção plena, tendo colocado de lado a cobiça e o desprazer
pelo mundo. Por qual razão? Eu digo que esse é um corpo entre os corpos,
Ananda, ou seja, a inspiração e a expiração. Portanto, Ananda, nessa ocasião, o
bhikkhu permanece contemplando o corpo como um corpo, ardente, plenamente
consciente e com atenção plena, tendo colocado de lado a cobiça e o desprazer
pelo mundo.<o:p></o:p></span></p>



<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-language:PT-BR'>(II. Contemplação das sensações)<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sempre,
Ananda, que um bhikkhu treina assim: ‘Eu inspiro experienciando êxtase’; ele
treina assim: ‘eu expiro experienciando êxtase.’<a name=R4></a> Ele treina
assim:’eu inspiro experienciando a felicidade’; ele treina assim: ‘eu expiro
experienciando a felicidade’. Ele treina assim: ‘eu inspiro experienciando a formação da mente’; 
ele treina assim: ‘eu expiro experienciando a formação da mente.’ Ele treina assim: ‘eu inspiro tranqüilizando a formação da mente’; 
ele treina assim: eu expiro tranqüilizando a formação da mente’ - nessa ocasião, o bhikkhu permanece contemplando as
sensações como sensações, ardente, plenamente consciente e com atenção plena,
tendo colocado de lado a cobiça e o desprazer pelo mundo. Por qual razão? Eu
digo que essa é uma certa sensação entre as sensações, Ananda, ou seja,<span
style='mso-spacerun:yes'>  </span>a cuidadosa atenção à inspiração e à
expiração. Portanto, Ananda, nessa ocasião, o bhikkhu permanece contemplando as
sensações como sensações, ardente, plenamente consciente e com atenção plena,
tendo colocado de lado a cobiça e o desprazer pelo mundo.<o:p></o:p></span></p>



<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-language:PT-BR'>(III. Contemplação da mente)<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sempre,
Ananda, que um bhikkhu treina assim: ‘eu inspiro experienciando a mente’; ele
treina assim: ‘eu expiro experienciando a mente.’ Ele treina assim: ‘eu inspiro
satisfazendo a mente’;<span style='mso-spacerun:yes'>  </span>ele treina assim:
‘eu expiro satisfazendo a mente.’ Ele treina assim: ‘eu inspiro concentrando a
mente’; ele treina assim: eu expiro concentrando a mente.’Ele treina assim: ‘eu
inspiro libertando a mente’; ele treina assim: eu expiro libertando a mente’ -
nessa ocasião, o bhikkhu permanece contemplando a mente como mente, ardente,
plenamente consciente e com atenção plena, tendo colocado de lado a cobiça e o
desprazer pelo mundo. Por qual razão? Eu digo, Ananda, que não há o
desenvolvimento da concentração através da atenção plena na respiração naquele
que é desatento e desprovido de plena consciência.<span
style='mso-spacerun:yes'>  </span>Portanto, Ananda, nessa ocasião, o bhikkhu
permanece contemplando a mente como mente, ardente, plenamente consciente e com
atenção plena, tendo colocado de lado a cobiça e o desprazer pelo mundo.<o:p></o:p></span></p>


<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-language:PT-BR'>(IV. Contemplação dos objetos mentais)<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sempre,
Ananda, que um bhikkhu treina assim: ‘Eu<span style='mso-spacerun:yes'> 
</span>inspiro contemplando a impermanência’; ele treina assim: ‘eu expiro
contemplando a impermanência.’ Ele treina assim: ‘eu inspiro contemplando o
desaparecimento’; ele treina assim: ‘eu expiro contemplando o desaparecimento.’
Ele treina assim: ‘eu inspiro contemplando a cessação’; ele treina assim: ‘eu
expiro contemplando a cessação.’ Ele treina assim: ‘eu inspiro contemplando a
renúncia’; ele treina assim: ‘eu expiro contemplando a renúncia’ - nessa
ocasião, o bhikkhu permanece contemplando os objetos mentais como objetos
mentais, ardente, plenamente consciente e com atenção plena, tendo colocado de
lado a cobiça e o desprazer pelo mundo. Tendo visto através da sabedoria o
abandono da cobiça e desprazer, ele é aquele que observa atento com
equanimidade. [<a href="#N2">2</a>] <a name=R2></a>Portanto, Ananda, nessa
ocasião, o bhikkhu permanece contemplando os objetos mentais como objetos
mentais, ardente, plenamente consciente e com atenção plena, tendo colocado de
lado a cobiça e o desprazer pelo mundo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Suponha,
Ananda, que numa encruzilhada houvesse um grande amontoado de terra. Se uma
carroça ou carruagem viesse do leste, oeste, norte ou sul, <a name=PVW>iria</a>
aplanar aquele amontoado de terra. [<a href="#N3">3</a>] <a name=R3></a>Da mesma
forma, Ananda, quando um bhikkhu permanece contemplando o corpo como um corpo,
as sensações como sensações, a mente como mente, os objetos mentais como
objetos mentais, ele aplana os estados ruins e prejudiciais.”<o:p></o:p></span></p>



