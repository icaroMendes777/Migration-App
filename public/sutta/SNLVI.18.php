<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'> Então, um certo bhikkhu foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse: “Venerável senhor, dizem, ‘verdadeiro conhecimento, verdadeiro conhecimento.’ O que é o verdadeiro conhecimento, venerável senhor, e de que modo alguém alcançou o verdadeiro conhecimento?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'> “Bhikkhu, compreender o sofrimento, compreender a origem do sofrimento, compreender a cessação do sofrimento, compreender o caminho que conduz à cessação do sofrimento: isso é chamado de verdadeiro conhecimento, bhikkhu, e é desse modo que alguém  alcança o verdadeiro conhecimento. <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
