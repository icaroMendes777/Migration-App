<p class=Normal> [Uma devata:] </p>

<p class=Normal style='margin-left:.75in'> “Se um bhikkhu é um arahant, <br>
perfeito, com as impurezas destruídas, <br>
alguém que possui o seu último corpo, <br>
ele ainda assim diria, ‘Eu falo’? <br>
e ele diria, ‘Eles falam comigo’?”[<a href="#N1">1</a>] </p>

<p class=Normal><a name="R1"> </a> [O Abençoado:] </p>

<p class=Normal style='margin-left:.75in'> “Quando um bhikkhu é um arahant,<br>
perfeito, com as impurezas destruídas, <br>
alguém que possui o seu último corpo, <br>
ele poderá dizer, ‘Eu falo.’ <br>
e ele poderá dizer, ‘Eles falam comigo.’ <br>
Hábil, compreendendo a linguagem de uso corrente no mundo, <br>
ele emprega esses termos como meras expressões.” [<a href="#N2">2</a>] </p>

<p class=Normal><a name="R2"></a> [Devata:] </p>

<p class=Normal style='margin-left:.75in'> “Quando um bhikkhu é um arahant,<br>
perfeito, com as impurezas destruídas, <br>
alguém que possui o seu último corpo, <br>
é porque ele se encontrou com a presunção<br>
que ele diria, ‘Eu falo,’ <br>
que ele diria, ‘Eles falam comigo’?”[ <a href="#Ne">3</a>] </p>

<p class=Normal><a name="R3"></a> [O Abençoado:] </p>

<p class=Normal style='margin-left:.75in'> “Aquele que abandonou por completo<br>
a cadeia de ilusões com relação ao eu, <br>
esses vínculos foram dispersos, não existem mais. <br>
Rico em sabedoria, ele escapou para mais além <br>
das concepções e suposições da mente errante. <br> 
Ele ainda poderá dizer, ‘Eu falo,’ <br>
ele também poderá dizer, ‘Eles falam comigo.’ <br>
Hábil, compreendendo a linguagem de uso corrente no mundo, <br>
ele emprega esses termos como meras expressões.” </p>
 
