<p class=Normal><b>Notas: </b></p>
 
<p class=Normal><a name="N1"></a>[1] Este deva que habitava um bosque, ouviu os bhikkhus na floresta empregando expressões como “eu como, eu sento, meu manto, minha tigela, etc.” Pensando, “Eu pensei que esses bhikkhus fossem arahants, mas os arahants podem falar de uma forma que faça supor a idéia da existência de um eu?” O deva foi até o Buda e formulou a sua pergunta. [<a href="#R1">Retorna</a>] </p>

<p class=Normal><a name="N2"></a> [2] Veja também o <a href="DN9.php#T53">DN 9.53</a> : “Mas, Citta, esses são apenas nomes, expressões, modos de linguagem, designações de uso corrente no mundo, que o Tathagata usa sem se agarrar nelas.” [<a href="#R2">Retorna</a>] </p>

<p class=Normal><a name="N3"></a> [3] De acordo com o comentário, neste ponto o deva pensou que enquanto que os arahants não falariam dessa forma devido a uma idéia (de um eu), eles poderiam assim fazer porque ainda tinham presunção ( isto é, <i>asmimana</i>, a presunção “eu sou”). Por conseguinte ele formula a segunda pergunta e o Buda responde indicando que os arahants abandonaram por completo a presunção. [<a href="#R3">Retorna</a>] </p>


