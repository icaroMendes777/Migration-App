<p class=Normal> “Bhikkhus, como um pote que tenha sido virado de cabeça para baixo abandona toda a sua água e não a toma de volta, assim também um bhikkhu que desenvolve e cultiva os quatro fundamentos da atenção plena abandona todos os estados ruins e prejudiciais e não os toma de volta.[<a href="#N1">1</a>]<o:p></o:p></span></p>


<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'> “E como um bhikkhu faz isso? Neste caso, bhikkhus, um bhikkhu permanece contemplando o corpo como um corpo, ardente, plenamente consciente e com atenção plena, tendo colocado de lado a cobiça e o desprazer pelo mundo. Ele permanece contemplando as sensações como sensações ... mente como mente ... objetos mentais como objetos mentais, ardente, plenamente consciente e com atenção plena, tendo colocado de lado a cobiça e o desprazer pelo mundo. É desse modo, bhikkhus, que um bhikkhu desenvolve e cultiva os quatro fundamentos da atenção plena para ele abandonar todos os estados ruins e prejudiciais e não os tomar de volta.”<o:p></o:p></span></p>

