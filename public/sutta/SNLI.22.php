<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] O corpo feito pela mente, <i>(<a name=PVW>manomayakaya</a>), </i>é
um corpo sutil criado do corpo físico por um meditador que tem maestria sobre
o<span style="mso-spacerun: yes">  </span>quarto jhana. No <a href="DN2.php#T85">DN
2.85</a> ele é descrito como “dotado de forma, feito pela mente, completo com
todas as suas partes, sem defeito em nenhuma das faculdades”. [<a href="#R1">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[2] “Uma percepção bem-aventurada e uma percepção leve”: esta é a
percepção associada com a mente do conhecimento direto; é uma percepção
bem-aventurada porque está acompanhada pela paz sublime (da mente), e é uma
percepção leve porque não há a inibição das contaminações. [<a href="#R2">Retorna</a>]<o:p></o:p></span></p>





