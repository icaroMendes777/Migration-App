<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. Então, o venerável Ananda foi até o Abençoado e depois de
cumprimentá-lo sentou a um lado e disse: <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, o Abençoado se recorda de alguma vez ter ido até o mundo de brahma
através do poder espiritual com um corpo feito pela mente?” [<a href="#N1">1</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Eu me recordo, Ananda, de ter ido até o mundo de brahma através do
poder espiritual com um corpo feito pela mente.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas,
venerável senhor, o Abençoado se recorda de alguma vez ter ido até o mundo de
brahma através do poder espiritual com este corpo composto dos quatro grandes
elementos?” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu me
recordo, Ananda, de ter ido até o mundo de brahma através do poder espiritual
com este corpo composto dos quatro grandes elementos.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Que o
Abençoado seja capaz de ir até o mundo de brahma através do poder espiritual
com um corpo feito pela mente, e que ele se recorde de ter ido até o mundo de
brahma através do poder espiritual com este corpo composto dos quatro grandes
elementos: isso é maravilhoso e admirável, venerável senhor, por parte do
Abençoado.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Os
Tathagatas, Ananda, são maravilhosos e possuem qualidades maravilhosas; os
Tathagatas são admiráveis e possuem qualidades admiráveis.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando,
Ananda, o Tathagata imerge o corpo na mente e a mente no corpo, e quando ele
permanece, depois de ter entrado, numa percepção bem-aventurada e numa
percepção leve com relação ao corpo, nessa ocasião o corpo do Tathagata se
torna mais leve, maleável, flexível e luminoso. [<a href="#N2">2</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Assim como uma bola de ferro, Ananda, aquecida durante todo o dia, se
torna mais leve, maleável, flexível e luminosa, quando o Tathagata imerge o
corpo na mente e a mente no corpo, e quando ele permanece, depois de ter
entrado, numa percepção bem-aventurada e numa percepção leve com relação ao
corpo, nessa ocasião também, o corpo do Tathagata se torna mais leve, maleável,
flexível e luminoso.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando,
Ananda, o Tathagata imerge o corpo na mente e a mente no corpo, e quando ele
permanece, depois de ter entrado, numa percepção bem-aventurada e numa
percepção leve com relação ao corpo, nessa ocasião, o corpo do Tathagata se
eleva sem dificuldade da terra para o ar. Ele exerce os vários tipos de poderes
supra-humanos: tendo sido um, ele se torna vários; tendo sido vários, ele se
torna um; ele aparece e desaparece; ele cruza sem nenhum problema uma parede,
um cercado, uma montanha ou através do espaço; ele mergulha e sai da terra como
se fosse água; ele caminha sobre a água sem afundar como se fosse terra;
sentado de pernas cruzadas ele cruza o espaço como se fosse um pássaro; com a
sua mão ele toca e acaricia a lua e o sol tão forte e poderoso; ele exerce
poderes corporais até mesmo nos distantes mundos de Brahma.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Como,
Ananda, um tufo de algodão, sendo leve e sustentado pelo vento, se eleva sem
dificuldade da terra para o ar, quando o Tathagata imerge o corpo na mente e a
mente no corpo, e quando ele permanece, depois de ter entrado, numa percepção
bem-aventurada e numa percepção leve com relação ao corpo, nessa ocasião
também, o corpo do Tathagata se eleva sem dificuldade da terra para o ar. Ele
exerce os vários tipos de poderes supra-humanos: tendo sido um, ele se torna
vários ... ele exerce poderes corporais até mesmo nos distantes mundos de
Brahma.”<o:p></o:p></span></p>


