formações ... consciência, através do apego à consciência, devido à adesão à
consciência, uma idéia como essa surge: ‘O mundo é infinito.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando, bhikkhus,
