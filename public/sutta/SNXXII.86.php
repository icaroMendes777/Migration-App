<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ouvi que em
certa ocasião o Abençoado estava próximo a Vesali, na Grande Floresta, no Salão
com um pico na cumeeira. Naquela ocasião, o ven. Anuradha estava não muito
distante do Abençoado numa cabana na floresta. <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, um
grande número de errantes, membros de outra seita, foram até o ven. Anuradha e
ao chegar eles se cumprimentaram. Depois da troca de saudações amigáveis e
corteses, eles sentaram a um lado e disseram:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigo
Anuradha, o Tathagata – o homem supremo, o homem superlativo, que realizou a
realização superlativa – ao ser descrito, ele é descrito de uma dessas quatro
maneiras: o Tathagata existe após a morte; não existe após a morte; ambos,
existe e não existe após a morte; nem existe, nem não existe após a morte.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isso
foi dito, o ven. Anuradha disse para os errantes membros de outra seita,
“Amigos, o Tathagata – o homem supremo, o homem superlativo, que realizou a
realização superlativa – ao ser descrito, ele é descrito de uma maneira
distinta dessas quatro: o Tathagata existe após a morte; não existe após a
morte; ambos, existe e não existe após a morte; nem existe, nem não existe após
a morte.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isso
foi dito, os errantes membros de outra seita disseram para o ven. Anuradha,
“Este bhikkhu é um novato, que não está na vida santa faz muito tempo; ou se
for um bhikkhu sênior, ele deve ser tolo e incompetente.” Assim, os errantes de
outra seita, tratando o ven. Anuradha como se ele fosse um novato ou um tolo,
levantaram dos seus assentos e partiram.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, não
muito tempo depois que os errantes de outra seita haviam partido, o seguinte
pensamento ocorreu ao ven. Anuradha: “Se eu for novamente questionado por esses
errantes de outra seita, como responderei de modo que fale de acordo com aquilo
que foi dito pelo Abençoado, sem deturpá-lo com algo contrário aos fatos,
explicando de acordo com o Dhamma, de tal modo que nada que dê margem à censura
possa de forma legítima ser deduzido da minha declaração?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, o
ven. Anuradha foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e
disse: “Agora mesmo eu estava não muito distante do Abençoado numa cabana na
floresta. Então, um grande número de errantes de outra seita vieram e...me
disseram, ‘Amigo Anuradha, o Tathagata – o homem supremo, o homem superlativo,
que realizou a realização superlativa – ao ser descrito, é descrito de uma
dessas quatro maneiras: o Tathagata existe após a morte; não existe após a
morte; ambos, existe e não existe após a morte; nem existe, nem não existe após
a morte.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando
isso foi dito, eu disse, ‘Amigos, o Tathagata – o homem supremo, o homem
superlativo, que realizou a realização superlativa – ao ser descrito, é
descrito de uma maneira distinta dessas quatro: o Tathagata existe após a
morte; não existe após a morte; ambos, existe e não existe após a morte; nem
existe, nem não existe após a morte.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando
isso foi dito, os errantes de outra seita me disseram, ‘Este bhikkhu é um
novato, que não está na vida santa faz muito tempo; ou se for um bhikkhu
sênior, ele deve ser tolo e incompetente.” Assim, tratando-me como se eu fosse
um novato ou um tolo, eles se levantaram dos seus assentos e partiram.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, não
muito tempo depois que os errantes de outra seita haviam partido, o seguinte
pensamento me ocorreu: “Se eu for novamente questionado por esses errantes de
outra seita, como responderei de modo que fale de acordo com aquilo que foi
dito pelo Abençoado, sem deturpá-lo com algo contrário aos fatos, explicando de
acordo com o Dhamma, de tal modo que nada que dê margem à censura possa de
forma legítima ser deduzido da minha declaração?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O que você
pensa, Anuradha, a forma é permanente ou impermanente?<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Impermanente, senhor. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;E aquilo que é
impermanente é sofrimento ou felicidade? <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sofrimento, senhor.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E é adequado considerar o
que é impermanente, sofrimento, sujeito a mudanças como: ‘Isso é meu. Isso sou eu.
Isso é o meu eu’? <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não, senhor.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“...a sensação é permanente
ou impermanente?<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Impermanente, senhor.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“... a percepção é
permanente ou impermanente?<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Impermanente, senhor.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“... as formações volitivas são
permanentes ou impermanentes?<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Impermanentes, senhor. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O que você pensa,
Anuradha, a consciência é permanente ou impermanente? <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Impermanente, senhor. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E aquilo que é
impermanente é sofrimento ou felicidade?<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sofrimento, senhor.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E é adequado considerar o
que é impermanente, sofrimento, sujeito a mudanças como: 'Isso é meu. Isso sou
eu. Isso é o meu eu’?<span style="mso-spacerun: yes">  </span><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não, senhor. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O que você pensa, você
considera a forma como sendo o Tathagata?<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não, senhor.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Você considera a sensação
como sendo o Tathagata<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não, senhor.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Você considera a percepção
como sendo o Tathagata<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não, senhor.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Você considera as
formações volitivas como sendo o Tathagata<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não, senhor.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Você considera a
consciência como sendo o Tathagata<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não, senhor.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O que você pensa, você
considera que o Tathagata está na forma? ... Separado da forma? ... Na
sensação? ... Separado da sensação? ... Na percepção? ... Separado da
percepção? ... Nas formações volitivas? ... Separado das formações volitivas? ... Na consciência?
... Separado da consciência?&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não, senhor.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O que você pensa, você
considera que o Tathagata é o conjunto da forma-sensação-percepção-formações volitivas-consciência?
<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não, senhor.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Você considera que o
Tathagata não tem forma, não tem sensação, não tem percepção, não tem
formações volitivas, não tem consciência? <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
senhor.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas,
Anuradha, quando você não consegue determinar o Tathagata como real e presente
nesta mesma vida, é apropriado que você declare, ‘Amigos, o Tathagata – o homem
supremo, o homem superlativo, que realizou a realização superlativa – ao ser
descrito, é descrito de uma maneira distinta dessas quatro: o Tathagata existe
após a morte; não existe após a morte; ambos, existe e não existe após a morte;
nem existe, nem não existe após a morte.’?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Muito bem,
Anuradha. Tanto antes, como agora, eu declaro somente o sofrimento e a cessação
do sofrimento.”<o:p></o:p></span></p>


