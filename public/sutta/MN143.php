<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim ouvi.
Em certa ocasião o Abençoado estava em Savatthi, no Bosque de Jeta, no Parque de
Anathapindika. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. Agora,
naquela ocasião o chefe de família Anathapindika estava atormentado, sofrendo e
gravemente enfermo. Então, ele se dirigiu a um homem desta forma: &quot;Venha,
bom homem, vá até o Abençoado, homenageie-o em meu nome com a sua cabeça aos
pés dele e diga: Venerável senhor, o chefe de família Anathapindika está
atormentado, sofrendo e gravemente enfermo; ele homenageia o Abençoado com a
cabeça aos seus pés.' Depois vá até o Venerável Sariputta, homenageie-o em meu
nome com a sua cabeça aos pés dele e diga: 'Venerável senhor, o chefe de
família Anathapindika está atormentado, sofrendo e gravemente enfermo; ele
homenageia o Venerável Sariputta com a cabeça aos seus pés.' Então diga: 'Seria
bom, venerável senhor, se o Venerável Sariputta fosse até a residência do chefe
de família Anathapindika, por compaixão.'&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Sim
senhor,&quot; o homem respondeu, e foi até onde estava o Abençoado, após
homenageá-lo, ele sentou a um lado e relatou a sua mensagem. Depois ele foi até
o Venerável Sariputta e após homenageá-lo ele relatou a sua mensagem, dizendo:
&quot;Seria bom, venerável senhor, se o Venerável Sariputta fosse até a
residência do chefe de família Anathapindika, por compaixão.&quot; O Venerável
Sariputta consentiu em silêncio.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. Então o
Venerável Sariputta se vestiu e tomando a tigela e o manto externo, foi para a
residência do chefe de família Anathapindika tendo o Venerável Ananda como seu
acompanhante. Tendo chegado, ele sentou em um assento que havia sido preparado
e disse para o chefe de família Anathapindika: &quot;Eu espero que você esteja
melhorando, chefe de família, espero que você esteja confortável, espero que as
suas sensações de dor estejam diminuindo e não aumentando e que<span
style="mso-spacerun: yes">  </span>a sua diminuição, não o seu aumento, seja
evidente.&quot;<o:p></o:p></span></p>



<p class=Normal><a name=T4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>4. &quot;Venerável Sariputta, eu não estou melhorando, não me sinto
confortável. Minhas sensações de dor estão aumentando, não diminuindo, o seu
aumento, não a sua diminuição é evidente. Tal como se um homem forte estivesse
partindo a minha cabeça com uma espada afiada, da mesma forma, ventos violentos
atravessam a minha cabeça. Eu não estou melhorando ... Tal como se um homem
forte estivesse apertando uma correia de couro dura em volta da minha cabeça,
da mesma forma, sinto dores violentas na minha cabeça. Eu não estou melhorando
... Tal como se um açougueiro habilidoso ou seu aprendiz cortassem o ventre de um
boi com uma faca de açougueiro afiada, da mesma forma, ventos violentos estão
cortando o meu ventre. Eu não estou melhorando ... Tal como se dois homens fortes
agarrassem um homem mais fraco por ambos braços e o assassem sobre uma cova com
carvão em brasa, da mesma forma, um fogo violento queima no meu corpo. Eu não
estou melhorando, não me sinto confortável. Minhas sensações de dor estão
aumentando, não diminuindo, o seu aumento, não a sua diminuição é
evidente.&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5.
&quot;Então chefe de família, você deve praticar da seguinte forma: 'Eu não me
apegarei ao olho e a minha consciência não dependerá do olho.' [<a href="#N1">1</a>]
<a name=R1></a>Assim você deve praticar. Você deve praticar da seguinte forma:
'Eu não me apegarei ao ouvido ... Eu não me apegarei ao nariz ... Eu não me
apegarei à língua ... Eu não me apegarei ao corpo ... Eu não me apegarei à mente e
a minha consciência não dependerá da mente.' Assim você deve praticar. <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. “Chefe
de família, você deve praticar da seguinte forma: 'Eu não me apegarei às formas
... Eu não me apegarei aos sons ... Eu não me apegarei aos aromas ... Eu não me
apegarei aos sabores ... Eu não me apegarei aos tangíveis ... Eu não me apegarei
aos objetos mentais e a minha consciência não dependerá de objetos mentais.' Assim você
deve praticar.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. “Chefe
de família, você deve praticar da seguinte forma: 'Eu não me apegarei à
consciência no olho ... Eu não me apegarei à consciência no ouvido ... Eu não me
apegarei à consciência no nariz ... Eu não me apegarei à consciência na língua ...
Eu não me apegarei à consciência no corpo ... Eu não me apegarei à consciência na
mente e a minha consciência não dependerá da consciência na mente.’ Assim você
deve praticar.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. “Chefe
de família, você deve praticar da seguinte forma: 'Eu não me apegarei ao
contato no olho ... Eu não me apegarei ao contato no ouvido ... Eu não me apegarei
ao contato no nariz ... Eu não me apegarei ao contato na língua ... Eu não me
apegarei ao contato no corpo ... Eu não me apegarei ao contato na mente e a minha
consciência não dependerá do contato na mente.' Assim você deve praticar.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. “Chefe
de família, você deve praticar da seguinte forma: 'Eu não me apegarei à sensação
que surge tendo o contato no olho como condição ... Eu não me apegarei à sensação
que surge tendo o contato no ouvido como condição ... Eu não me apegarei à
sensação que surge tendo o contato no nariz como condição ... Eu não me apegarei
à sensação que surge tendo o contato na língua como condição ... Eu não me
apegarei à sensação que surge tendo o contato no corpo como condição ... Eu não me
apegarei à sensação que surge tendo o contato na mente como condição e a minha
consciência não dependerá da sensação que surge tendo o contato na mente como
condição.' Assim você deve praticar.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. “Chefe
de família, você deve praticar da seguinte forma: 'Eu não me apegarei ao
elemento terra ... Eu não me apegarei ao elemento água ... Eu não apegarei ao elemento
fogo ... Eu não me apegarei ao elemento ar ... Eu não apegarei ao elemento espaço ...
Eu não me apegarei ao elemento consciência e a minha consciência não dependerá
do elemento consciência.’ Assim você deve praticar. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. “Chefe
de família, você deve praticar da seguinte forma: 'Eu não me apegarei à forma
material ... Eu não me apegarei à sensação ... Eu não me apegarei à percepção ... Eu
não me apegarei às formações ... Eu não me apegarei à consciência e a minha
consciência não dependerá da consciência.’ Assim você deve praticar.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. “Chefe
de família, você deve praticar da seguinte forma: 'Eu não me apegarei à base do
espaço infinito ... Eu não me apegarei à base da consciência infinita ... Eu não
apegarei à base do nada ... Eu não me apegarei à base da nem percepção, nem não
percepção e a minha consciência não dependerá da base da nem percepção, nem não
percepção.’ Assim você deve praticar. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. “Chefe
de família, você deve praticar da seguinte forma: 'Eu não me apegarei a este
mundo e a minha consciência não dependerá deste mundo. Eu não me apegarei ao
mundo que está além e a minha consciência não dependerá do mundo que está
além.’ Assim você deve praticar.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14. “Chefe
de família, você deve praticar da seguinte forma: 'Eu não me apegarei ao
que é visto, ouvido, sentido, conscientizado, buscado, procurado, ponderado
pela mente e a minha consciência não dependerá disso.’ Assim você deve
praticar.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. Quando
isso foi dito o chefe de família Anathapindika chorou e derramou
lágrimas. Então o Venerável Ananda lhe perguntou: &quot;Você está cedendo,
chefe de família, você está afundando?&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Eu
não estou cedendo, Venerável Ananda, eu não estou afundando. Embora durante
muito tempo eu tenha servido o Mestre e bhikkhus dignos de apreciação, nunca
ouvi um discurso do Dhamma semelhante a este.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Um
discurso do Dhamma como este, chefe de família, não é dado para os discípulos
leigos que se vestem de branco. Este tipo de discurso do Dhamma é dado para as
pessoas que seguiram a vida santa.” [<a href="#N2">2</a>] <o:p></o:p></span></p>



<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>&quot;Pois então, Venerável Sariputta, permita que esse discurso do
Dhamma seja dado para os discípulos leigos que se vestem de branco. Existem
membros de clãs que possuem uma fina camada de poeira sobre os olhos, que estão
perdidos por não ouvirem esse tipo de discurso do Dhamma. Existem aqueles que
compreenderão o Dhamma.&quot; <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16. Assim,
depois de dar esse conselho ao chefe de família Anathapindika, o Venerável
Sariputta e o Venerável Ananda levantaram-se dos seus assentos e partiram.
Pouco depois que eles partiram o chefe de família Anathapindika morreu e
renasceu no paraíso de Tusita.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17. Então
quando a noite já estava bastante avançada, Anathapindika, agora um jovem deva
com uma bela aparência, foi até o Abençoado, iluminando todo o bosque de Jeta.
Depois de homenageá-lo ele ficou parado a um lado e dirigiu-se ao Abençoado em
versos: <o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'>
&quot;Oh, abençoado é este bosque de
Jeta, <br>

habitado pela sábia Sangha,<br>

onde reside o rei do Dhamma,<br>

a fonte de toda a minha felicidade.<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'>
Através da ação, conhecimento e Dhamma,<br>

através da virtude e modo de vida
nobre -<br>

através disso os mortais são
purificados,<br>

não através da linhagem ou riqueza.<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'>
Portanto uma pessoa sábia que vê<br>

aquilo que realmente conduz ao seu
próprio bem,<br>

deveria investigar o Dhamma<br>

e purificar-se através dele.<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'>
Sariputta alcançou o cume<br>

em virtude, paz e sabedoria;<br>

qualquer bhikkhu que foi mais além<br>

pode no máximo igualá-lo.&quot; <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18. Isso
foi o que o jovem deva Anathapindika disse e o Mestre aprovou. Então o jovem
deva Anathapindika, pensando: “O Mestre me aprovou,” homenageou o Abençoado e
mantendo-o à sua direita, desapareceu em seguida.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>19. Quando
a noite havia terminado, o Abençoado dirigiu-se aos bhikkhus desta forma:
&quot;Bhikkhus, ontem à noite, quando a noite já estava bem avançada, veio ter
comigo um certo deva com belíssima aparência que iluminou todo o bosque de
Jeta. Depois de me homenagear, ele ficou em pé a um lado e dirigiu-se a mim em
versos desta forma: <o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'>&quot; Oh, abençoado é este bosque
de Jeta ...<br>

pode no máximo igualá-lo.&quot; <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o
que o jovem deva disse. Então o jovem deva, pensando: 'O Mestre me aprovou,’ me
homenageou e mantendo-me à sua direita, desapareceu em seguida.&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>20. Quando
isso foi dito, o Venerável Ananda disse<span style="mso-spacerun: yes"> 
</span>ao Abençoado: &quot;Com certeza, venerável senhor, aquele jovem deva
deve ter sido Anathapindika. Pois o chefe de família Anathapindika tinha
perfeita confiança no Venerável Sariputta.&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Muito
bem, Ananda! Você deduziu a conclusão correta. Aquele jovem deva era
Anathapindika, ninguém mais.&quot;<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o
que disse o Abençoado. O Venerável Ananda ficou satisfeito e contente com as
palavras do Abençoado.<o:p></o:p></span></p>


