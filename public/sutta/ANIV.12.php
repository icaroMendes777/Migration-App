<p class=Normal> “Bhikkhus, permaneçam possuídos pela virtude, possuídos pelo Patimokkha, contidos pelas regras do Patimokkha, sejam perfeitos na sua conduta e na sua esfera de atividades, temendo a menor falha, treinem adotando os preceitos de virtude. Mas se um bhikkhu assim permanecer, o que mais deve ser feito? </p>

<p class=Normal> “Se enquanto estiver caminhando, em pé, sentado ou deitado, um bhikkhu estiver livre do desejo sensual, da má vontade, da preguiça e do torpor, da inquietação e da ansiedade e tiver descartado a dúvida, então a sua energia estará fortificada e impregnável; a sua atenção plena estará alerta e lúcida; o seu corpo estará tranqüilo e sem agitação; a sua mente estará concentrada e unificada. </p> 

<p class=Normal>Um bhikkhu que desse modo, de tempos em tempos, demonstra ardência e  vergonha de cometer transgressões, é chamado energético e determinado. </p>

<p class=Normal style='margin-left:.75in'>Controlado ao caminhar, controlado ao ficar em pé,<br>
controlado quando sentado, controlado quando deitado, <br>
controlado ao recolher os membros, <br>
controlado ao esticar os membros: <br>
acima, de lado a lado e abaixo, <br>
até onde se estende o mundo, <br>
um bhikkhu observa como as coisas ocorrem, <br>
como os agregados surgem e desaparecem. </p>

<p class=Normal style='margin-left:.75in'>Quando alguém assim permanece ardente<br>
com o comportamento calmo e tranqüilo, <br>
sempre com atenção plena, treinando<br>
na prática de tranqüilizar a mente, <br>
é dito de um bhikkhu com esse comportamento, <br>
“Aquele que sempre está determinado.” </p>

