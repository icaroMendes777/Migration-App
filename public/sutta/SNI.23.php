<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] <i>Jata, </i>enredo, é um termo para a rede do desejo no sentido de
que este “aprisiona”. Há um enredo interior e um enredo exterior, porque o desejo
surge com respeito às nossas posses e às posses dos outros; com respeito ao
nosso corpo e ao corpo dos outros; e com respeito às seis bases internas e
externas. [<a href="#R1">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[2] A resposta do Buda é um enunciado sucinto do treinamento tríplice,
com “desenvolvendo a mente”, (<i>citta), </i>referindo-se a<i> samadhi. </i>O
comentário adiciona que a sabedoria é mencionada três vezes neste verso:
primeiro, como a inteligência inata, (“sábio”); segundo, “desenvolvendo a
sabedoria”, como a sabedoria derivada do insight, (<i>vipassana-pañña)</i>; e
terceiro, “discernimento,” a sabedoria pragmática que assume a liderança em
todas as tarefas. [<a href="#R2">Retorna</a>] <i><span style="mso-spacerun:
yes"> </span><o:p></o:p></i></span></p>


<p class=Normal><a name=N3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[3] Enquanto o verso anterior mostrava o discípulo em treinamento, <i>(<a
name=PVW>sekha</a>), </i>que é capaz de desenredar o enredo, este verso mostra
o arahant, aquele que foi além do treinamento, que desenredou o enredo. [<a
href="#R3">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[4] Este verso mostra em que ponto o enredo pode ser desenredado. Neste
caso, mentalidade representa os quatro agregados mentais. A “colisão”
representa o impacto dos cinco objetos dos sentidos nas cinco bases sensuais. [<a
href="#R4">Retorna</a>]<o:p></o:p></span></p>



