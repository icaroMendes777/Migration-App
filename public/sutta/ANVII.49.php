<p class=Tit3 align=center style='text-align:center'><b> Anguttara Nikaya VII.49</b></Tit3>
<p class=Tit1 align=center style='text-align:center'>Dana Sutta </Tit1>
<p class=Tit1 align=center style='text-align:center'>Generosidade </Tit1>

<p class=Normal align=center style='text-align:center'><span lang=PT-BR style='font-size:
