PT-BR'>Nota de Thanissaro Bhikkhu:</span></b><span lang=PT-BR style='mso-ansi-language:
PT-BR'> Este discurso discute a motivação que a pessoa pode ter quando é
generosa, e classifica em ordem ascendente os resultados que as diferentes
motivações trazem. O Comentário nota que a motivação mais elevada não
contaminada por motivações menores e que conduz ao 'não retorno', exige um
certo nível de maestria na concentração e insight para que seja considerada uma
motivação genuína para a generosidade.<o:p></o:p></span></p>

