<p class=Tit3 align=center style='text-align:center'><b>Samyutta Nikaya III.1</b></Tit3>
<p class=Tit1 align=center style='text-align:center'>Dahara Sutta </Tit1>
<p class=Tit1 align=center style='text-align:center'>Jovem </Tit1>

<p class=Normal align=center style='text-align:center'><span lang=PT-BR style='font-size:
