<p class=Normal style='margin-left:.75in'>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim ouvi.
Em certa ocasião o Abençoado estava<span style="mso-spacerun: yes">  </span>em
Savatthi no Bosque de Jeta, no Parque de Anathapindika. Então, o Rei Pasenadi de
Kosala foi até o Abençoado e ambos se cumprimentaram. Quando a conversa cortês
e amigável havia terminado ele sentou a um lado e disse para o Abençoado: <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O Mestre
Gotama também afirma, ‘Eu despertei para a insuperável perfeita iluminação’?” [<a
href="#N1">1</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Se, grande rei, falando corretamente, alguém dissesse que, ‘Ele
despertou para a insuperável perfeita iluminação,’ é de mim verdadeiramente que,
falando o que é certo, isso deveria ser dito. Pois eu, grande rei, despertei
para a insuperável perfeita iluminação.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mestre
Gotama, mesmo estes contemplativos e brâmanes, sendo cada um o cabeça de uma
ordem, cabeça de um grupo, mestre de um grupo, um bem conhecido e famoso
fundador de uma seita, considerado por muitos como um santo, isto é, Purana
Kassapa, Makkhali Gosala, Ajita Kesakambalin, Pakudha Kaccayana, Sanjaya
Belatthiputta e o Nigantha Nataputta – mesmo esses, quando eu perguntei a eles
se eles haviam despertado para a insuperável perfeita iluminação, não afirmaram
ter feito isso. Então porque deveria o Mestre Gotama [fazer tal afirmação]
quando ele ainda é tão jovem e seguiu a vida santa faz pouco tempo?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Há quatro
coisas, grande rei, que não devem ser depreciadas e desprezadas como ‘jovens.’
Quais quatro? Um khattiya, grande rei, não deve ser depreciado e desprezado
como ‘jovem’; uma cobra não deve ser depreciada e desprezada como ‘jovem’; um
fogo não deve ser depreciado e desprezado como ‘jovem’; e um bhikkhu não deve
ser depreciado e desprezado como ‘jovem.’ Essas são as quatro coisas.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o
que o Abençoado disse. Dito isso, o Iluminado, o Mestre, disse mais:<o:p></o:p></span></p>



<p class=MsoNormal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Ele não deve desprezar como ‘jovem’ <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>um khattiya com nobre nascimento,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>um príncipe aristocrático com fama gloriosa:<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>um homem não deve desprezá-lo.<o:p></o:p></span></p>


<p class=MsoNormal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Pois pode acontecer que esse senhor de homens,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>esse khattiya, conquiste o trono,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>e enraivecido o golpeie duramente<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>com um castigo real.<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Por conseguinte, protegendo a própria vida,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>ele deveria evitá-lo.<o:p></o:p></span></p>


<p class=MsoNormal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Ele não deve desprezar como ‘jovem’<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>uma cobra que veja por acaso<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>no vilarejo ou na floresta:<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>um homem não deve desprezá-la.<o:p></o:p></span></p>


<p class=MsoNormal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Pois à medida que aquela feroz cobra desliza,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>manifestando-se em distintas formas, [<a href="#N2">2</a>]<br>

<a name=R2></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>esta poderá atacar e morder o tolo,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>quer seja homem ou mulher.<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Por conseguinte, protegendo a própria vida,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>ele deveria evitá-la.<o:p></o:p></span></p>


<p class=MsoNormal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Ele não deve desprezar como ‘jovem’<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>um fogo ardente que devora tudo,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>uma conflagração que deixa um rastro enegrecido:<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>um homem não deve desprezá-lo.<o:p></o:p></span></p>


<p class=MsoNormal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Pois se o fogo obtiver muito combustível,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>tendo se tornado uma conflagração,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>poderá atacar e queimar o tolo,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>quer seja homem ou mulher.<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Por conseguinte, protegendo a própria vida,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>ele deveria evitá-lo.<o:p></o:p></span></p>


<p class=MsoNormal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Quando um fogo devora uma floresta -<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>naquela conflagração com um rastro enegrecido -<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>os brotos novamente ressurgem<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>à medida que passem os dias e as noites.<o:p></o:p></span></p>


<p class=MsoNormal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Mas se um bhikkhu com a virtude consumada<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>queimar a outrem com o fogo [das suas virtudes],<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>ele não ganhará filhos e gado,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>nem os seus herdeiros adquirirão riqueza.<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Sem filhos e sem herança eles se tornam,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>tal como troncos de palmeira. [<a href="#N3">3</a>]<o:p></o:p></span></p>


<p class=MsoNormal style='margin-left:.75in'><a name=R3></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>“Por conseguinte, uma pessoa que é sábia,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>considerando o seu próprio bem,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>deve sempre tratá-los com propriedade:<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>uma cobra feroz e um fogo ardente,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>um khattiya famoso,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>e um bhikkhu com a virtude consumada.”<span style="mso-spacerun:
yes">       </span><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isso
foi dito, o Rei Pasenadi de Kosala disse para o Abençoado: “Magnífico,
Venerável Senhor! Magnífico, Venerável Senhor! O Abençoado esclareceu o Dhamma
de várias formas, como se tivesse colocado em pé o que estava de cabeça para
baixo, revelasse o que estava escondido, mostrasse o caminho para alguém que
estivesse perdido ou segurasse uma lâmpada no escuro para aqueles que possuem
visão pudessem ver as formas. Eu busco refúgio no Abençoado, no Dhamma e na
Sangha dos bhikkhus. Que o Abençoado se recorde de mim como um discípulo leigo
que nele buscou refúgio para o resto da sua vida.”<span style='mso-tab-count:1'>   </span><o:p></o:p></span></p>


