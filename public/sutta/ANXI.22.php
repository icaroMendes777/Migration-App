<p class=Normal> (22-29) “Bhikkhus, quando um pastor possui onze fatores, ele é incapaz de manter e criar um rebanho de gado. Quais onze? É o caso em que um pastor não tem conhecimento da forma, ele não tem habilidade com as características, ele não consegue remover os ovos das moscas, ele não consegue tratar as feridas, ele não consegue fumigar o galpão, ele não sabe onde está a fonte d’água, ele não sabe o que deve ser bebido, ele não conhece a estrada, ele não tem habilidade com os pastos, ele não sabe como ordenhar e ele não demonstra uma veneração especial para com aqueles touros que são os pais e líderes do rebanho. Quando um pastor possui esses onze fatores ele é incapaz de manter e criar um rebanho de gado. </p>

