<p class=Normal>Em certa ocasião o Abençoado estava próximo a Rajagaha, na Montanha do Pico do Abutre. Então, cedo pela manhã, o Venerável Sariputta se vestiu e tomando a tigela e o manto externo, estava descendo da Montanha do Pico do Abutre com um grande grupo de bhikkhus quando ele viu uma grande pilha de madeira a um lado. Vendo aquilo, ele disse para os bhikkhus: “Amigos, vocês vêm ali aquela grande pilha de madeira?” </p>

<p class=Normal> “Sim, amigo,” os bhikkhus responderam. </p>

<p class=Normal> “Amigos, se ele assim quisesse, um bhikkhu com poderes supra-humanos, tendo obtido maestria da sua mente, poderia determinar que aquela pilha de madeira fosse nada mais do que terra. Por que isso? Há o elemento terra naquela pilha de madeira, na dependência do qual ele poderia determinar que aquela pilha fosse nada além de terra. </p>

<p class=Normal> “Se ele assim quisesse, um bhikkhu com poderes supra-humanos, tendo obtido maestria da sua mente, poderia determinar que aquela pilha de madeira fosse nada mais do que água ... fogo ... ar ... belo ... repulsivo. Por que isso?  Há o elemento repulsivo naquela pilha de madeira, na dependência do qual ele poderia determinar que aquela pilha fosse nada além de repulsiva.” </p>

