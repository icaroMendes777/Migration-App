<p class=Normal> Certa ocasião o Abençoado estava perambulando por Kosala quando chegou em Kapilavatthu. O Sakya Mahanama ouviu: "O Abençoado chegou em Kapilavatthu." Então Mahanama foi até o Abençoado e depois de cumprimentá-lo ficou em pé a um lado. O Abençoado então disse: </p>

<p class=Normal>"Vá, Mahanama, encontre uma hospedaria em Kapilavatthu onde possamos passar a noite." </p>

<p class=Normal>"Sim, venerável senhor," Mahanama respondeu. Ele então foi até Kapilavatthu e procurou por toda a cidade mas não encontrou uma hospedaria adequada onde o Abençoado pudesse passar a noite. Assim ele foi novamente até o Abençoado e disse: "Venerável senhor, não há em Kapilavatthu uma hospedaria adequada onde o Abençoado possa passar a noite. Mas o Kalama Bharandu, que foi um companheiro do Abençoado, está aqui. [<a href="#N1">1</a>] <a name=R1></a>Que o Abençoado pernoite no seu alojamento." </p>

<p class=Normal>"Vá, Mahanama, e prepare um leito para mim." </p>

<p class=Normal>"Sim, venerável senhor," Mahanama respondeu. Então ele foi até o alojamento de Bharandu, preparou o leito, colocou água para lavar os pés e depois voltou até onde estava o Abençoado e disse: </p>

<p class=Normal>"Venerável senhor, preparei o leito e coloquei a água para lavar os pés. O Abençoado poderá ir quando for conveniente." </p>

<p class=Normal>Então o Abençoado foi até o alojamento de Bharandu, sentou no assento que havia sido preparado e lavou os pés. Então Mahanama pensou: "Este não é o momento apropriado para ficar com o Abençoado pois ele está cansado. Eu o visitarei amanhã. Então, depois de homenagear o Abençoado, mantendo-o à sua direita, ele partiu. </p>

<p class=Normal>Depois, quando a noite havia terminado, Mahanama foi até o Abençoado e depois de cumprimentá-lo sentou a um lado. O Abençoado disse: </p>

<p class=Normal>"Mahanama, há três tipos de professores que podem ser encontrados no mundo? Quais três? </p>

<p class=Normal> (1) "Aqui, Mahanama, um professor declara a completa compreensão dos prazeres dos sentidos, mas não das formas ou sensações. (2) Outro professor declara a completa compreensão dos prazeres dos sentidos e das formas, mas não das sensações. (3) Outro professor declara a completa compreensão dos prazeres dos sentidos, das formas e das sensações. Esses são os três tipos de professores que podem ser encontrados no mundo. O objetivo desses três tipos de professores é o mesmo ou diferente?" </p>

<p class=Normal>Quando isso foi dito, Bharandu disse para Mahanama: "Diga o mesmo, Mahanama." Mas o Abençoado disse para Mahanama: "Diga diferente, Mahanama." </p>

<p class=Normal>Uma segunda vez .... uma terceira vez, Bharandu disse para Mahanama: "Diga o mesmo, Mahanama." Mas o Abençoado disse para Mahanama: "Diga diferente, Mahanama." </p>

<p class=Normal>Então Bharandu pensou: O contemplativo Gotama me contradizeu três vezes na frente do influente Sakya Mahanama. Melhor eu deixar Kapilavatthu." </p>

<p class=Normal>Então Bharandu deixou Kapilavatthu. Tendo partido de Kapilavatthu, ele foi de vez e nunca mais retornou.</p>

