<p class=Normal> Assim ouvi. Certa ocasião o Abençoado estava em Rajagaha no Bambual no Santuário dos Esquilos. Então Sona, o filho de um chefe de família, foi até o Abençoado e depois de cumprimentá-lo ele sentou a um lado e o Abençoado disse: </p>


<p class=Normal> "Sona, aqueles contemplativos e brâmanes que não compreendem a forma, a sua origem, a sua cessação, e o caminho que conduz à sua cessação; que não compreendem a sensação ... a percepção ... as formações volitivas ... a consciência, a sua origem, a sua cessação, e o caminho que conduz à sua cessação: esses eu não considero como contemplativos entre os contemplativos  ou brâmanes entre os brâmanes, e esses veneráveis sem realizar por si mesmos através do conhecimento direto, nesta mesma vida entram e permanecem no objetivo dos contemplativos ou no objetivo dos brâmanes. </p>


