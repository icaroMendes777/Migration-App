<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião o Abençoado estava em Savatthi, no Bosque de Jeta, no
Parque de Anathapindika. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. Então o
estudante brâmane Subha, o filho de Todeyya, foi até o Abençoado e ambos se
cumprimentaram. [<a href="#N1">1</a>] <a name=R1></a>Quando a conversa amigável
e cortês havia terminado, ele sentou a um lado e perguntou ao Abençoado: <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3.
&quot;Mestre Gotama, qual é a causa e condição pela qual os seres humanos são
vistos como inferiores ou superiores? Pois, as pessoas podem ter vida curta ou
vida longa, podem ser doentias ou saudáveis, feias ou bonitas, sem poder ou
poderosas, pobres ou ricas, nascidas em classes inferiores ou superiores, tolas
ou sábias. Qual é a causa e condição, Mestre Gotama, pela qual os seres humanos
são vistos como inferiores ou superiores?&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. &quot;Estudante,
os seres são os donos das suas ações, herdeiros das suas ações, nascem das suas
ações, estão atados às suas ações, possuem as suas ações como refúgio. São as
ações que distinguem os seres entre inferiores ou superiores.&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Eu
não compreendo em detalhe o significado da afirmação do Mestre Gotama, aquilo
que foi dito de forma resumida sem explicar o significado em detalhe. Seria bom
se o Mestre Gotama me ensinasse o Dhamma de forma que eu pudesse entender em
detalhe o significado da afirmação do Mestre Gotama.&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Então,
estudante, ouça e preste muita atenção ao que vou dizer.&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Sim,
senhor,&quot; o estudante brâmane <a name=PVW>Subha</a> respondeu. O Abençoado
disse o seguinte: <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5.
&quot;Neste caso, estudante, um homem ou mulher tira a vida de outros seres, um
homicida, sanguinário, dedicado a golpes e violência, demonstrando nenhuma
piedade com os seres vivos. Porque ele realiza e se empenha em tal ação, com a
dissolução do corpo, após a morte, ele renasce num estado de privação, num
destino infeliz, nos reinos inferiores, até mesmo no <span style='mso-bidi-font-weight:
bold'>inferno</span>. Mas se na dissolução do corpo, após a morte, ele não
renascer num estado de privação, num destino infeliz, nos reinos inferiores,
até mesmo no <span style='mso-bidi-font-weight:bold'>inferno</span>, e ao invés
disso ele retornar ao estado humano, então onde quer que ele nasça, terá vida
curta. [<a href="#N2">2</a>] <a name=R2></a>Esse é o modo, estudante, que
conduz à vida curta, isto é, a pessoa tira a vida de outros seres, um homicida,
sanguinário, dedicado a golpes e violência, demonstrando nenhuma piedade com os
seres vivos.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. “Mas
neste caso, estudante, um homem ou mulher abandonando tirar a vida de outros
seres, se abstém de tirar a vida de outros seres; ele permanece com a sua vara
e arma postas de lado, bondoso e gentil, compassivo com todos os seres vivos.
Porque ele realiza e se empenha em tal ação, com a dissolução do corpo, após a
morte, ele renasce num destino feliz, até mesmo no paraíso. Mas se na
dissolução do corpo, após a morte, ele não renascer num destino feliz, no
paraíso, e ao invés disso ele retornar ao estado humano, então onde quer que
ele nasça, terá vida longa [<a href="#N3">3</a>] <a name=R3></a>Esse é o modo,
estudante, que conduz à vida longa, isto é, a pessoa deixa de matar seres
vivos, se abstém de matar seres vivos; com a vara e a arma postas de lado,
gentil e afável, ela permanece compassiva com todos os seres vivos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7.
&quot;Neste caso, estudante, um homem ou mulher é dado a ferir os seres com a
mão, com pedras, com um pau, ou com uma faca. Porque ele realiza e se empenha
em tal ação, com a dissolução do corpo, após a morte, ele renasce num estado de
privação … e ao invés disso ele retornar ao estado humano, então onde quer que
ele nasça, será doentio. Esse é o modo, estudante, que conduz à falta de
saúde, isto é, a pessoa é dada a ferir seres com a mão, com pedras, com um pau,
ou com uma faca.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. “Mas
neste caso, estudante, um homem ou mulher, não é dado a ferir seres com a mão,
com pedras, com um pau, ou com uma faca. Porque ele realiza e se empenha em tal
ação, com a dissolução do corpo, após a morte, ele renasce num destino feliz
... e ao invés disso ele retornar ao estado humano, então onde quer que ele
nasça, será saudável. Esse é o modo, estudante, que conduz à saúde, isto é, a
pessoa não é dada a ferir seres com a mão, com pedras, com um pau, ou com uma
faca.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9.
&quot;Neste caso, estudante, um homem ou mulher possui um caráter raivoso e
irritadiço; mesmo quando criticado apenas um pouco, ele se ofende, se torna
enraivecido, hostil e ressentido e demonstra raiva, ódio e amargor. Porque ele
realiza e se empenha em tal ação … ele renasce num estado de privação … e ao
invés disso ele retornar ao estado humano, então onde quer que ele nasça, será
feio. Esse é o modo, estudante, que conduz à feiúra, isto é, a pessoa possui
um caráter raivoso e irritadiço … e demonstra raiva, ódio e amargor.</span><span
lang=PT-BR style='font-size:11.0pt;mso-bidi-font-size:12.0pt;mso-ansi-language:
PT-BR'><o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10.
&quot;Mas neste caso, estudante, um homem ou mulher não possui um caráter
raivoso e irritadiço; mesmo quando muito criticado, ele não se ofende, não se
torna enraivecido, hostil e ressentido e não demonstra raiva, ódio e amargor.
Porque ele realiza e se empenha em tal ação … ele renasce num destino feliz … e
ao invés disso ele retornar ao estado humano, então onde quer que ele nasça,
será belo. Esse é o modo, estudante, que conduz à beleza, isto é, a pessoa não
possui um caráter raivoso e irritadiço… e não demonstra raiva, ódio e amargor. </span><span
lang=PT-BR style='font-size:11.0pt;mso-bidi-font-size:12.0pt;mso-ansi-language:
PT-BR'><span style='mso-spacerun:yes'> </span><o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11.
&quot;Neste caso, estudante, um homem ou mulher é invejoso, alguém que tem
inveja, ressentimento, fica enciumado com o ganho, honraria, respeito,
reverências, saudações e veneração recebidos pelos outros. Porque ele realiza e
se empenha em tal ação … ele renasce num estado de privação … e ao invés disso
ele retornar ao estado humano, então onde quer que ele nasça, não terá poder.
Esse é o modo, estudante, que conduz a não ter poder, isto é, a pessoa é
invejosa … fica enciumada com o ganho, honraria, respeito, reverências,
saudações e veneração recebidos pelos outros.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12.
&quot;Mas neste caso, estudante, um homem ou mulher não é invejoso, alguém que
não tem inveja, ressentimento, não fica enciumado com o ganho, honraria,
respeito, reverências, saudações e veneração recebidos pelos outros. Porque ele
realiza e se empenha em tal ação … ele renasce num destino feliz … e ao invés
disso ele retornar ao estado humano, então onde quer que ele nasça, terá poder.
Esse é o modo, estudante, que conduz ao poder, isto é, a pessoa não é invejosa
… não fica enciumada com o ganho, honraria, respeito, reverências, saudações e
veneração recebidos pelos outros. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13.
&quot;Neste caso, estudante, um homem ou mulher não dá alimentos, bebidas,
roupas, carruagens, ornamentos, perfumes, ungüentos, camas, moradias e
lamparinas para os contemplativos e brâmanes. Porque ele realiza e se empenha
em tal ação … ele renasce num estado de privação … e ao invés disso ele
retornar ao estado humano, então onde quer que ele nasça, será pobre. Esse é o
caminho, estudante, que conduz à pobreza, isto é, a pessoa não dá alimentos … e
lamparinas para os contemplativos e brâmanes.</span><span lang=PT-BR
style='font-size:11.0pt;mso-bidi-font-size:12.0pt;mso-ansi-language:PT-BR'><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14.
&quot;Mas neste caso, estudante, um homem ou mulher dá alimentos … e lamparinas
para os contemplativos e brâmanes. Porque ele realiza e se empenha em tal ação
… ele renasce num destino feliz …e ao invés disso ele retornar ao estado
humano, então onde quer que ele nasça, será rico. Esse é o modo, estudante,
que conduz à riqueza, isto é, a pessoa dá alimentos…e lamparinas para os
contemplativos e brâmanes.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15.
&quot;Neste caso, estudante, um homem ou mulher é obstinado e arrogante; ele não
demonstra respeito com quem merece respeito, ele não se levanta para aqueles em
cuja presença ele deve se levantar, não oferece o assento a quem merece um
assento, não oferece passagem a quem se deve dar passagem e não honra,
respeita, reverencia e venera alguém que deva ser honrado, respeitado,
reverenciado e venerado. Porque ele realiza e se empenha em tal ação … ele
renasce num estado de privação … e ao invés disso ele retornar ao estado
humano, então onde quer que ele nasça, nascerá numa classe inferior. Esse é o
modo, estudante, que conduz à classe inferior, isto é, a pessoa é obstinada e
arrogante … e não honra, respeita, reverencia e venera alguém que deva ser
honrado, respeitado, reverenciado e venerado.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16.
&quot;Mas neste caso, estudante, um homem ou mulher não é obstinado e
arrogante; ele demonstra respeito com quem merece respeito, ele se levanta para
aqueles em cuja presença ele deve se levantar, oferece o assento a quem merece
um assento, oferece passagem a quem se deve dar passagem e honra, respeita,
reverencia e venera alguém que deva ser honrado, respeitado, reverenciado e
venerado. Porque ele realiza e se empenha em tal ação … ele renasce num destino
feliz … e ao invés disso ele retornar ao estado humano, então onde quer que ele
nasça, nascerá numa classe superior. Esse é o modo, estudante, que conduz à
classe superior, isto é, a pessoa não é obstinada e arrogante … e<span
style='mso-spacerun:yes'>  </span>honra, respeita, reverencia e venera alguém
que deva ser honrado, respeitado, reverenciado e venerado<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17.
&quot;Neste caso, estudante, um homem ou mulher ao visitar um contemplativo ou
brâmane, não pergunta: 'Venerável senhor, o que é benéfico? O que é
prejudicial? O que é passível de crítica? O que não é passível de crítica? O
que deve ser cultivado? O que não deve ser cultivado? Que tipo de ação causará
o meu próprio dano e sofrimento por muito tempo? Que tipo de ação causará o meu
bem-estar e felicidade por muito tempo?' Porque ele realiza e se empenha em tal
ação … ele renasce num estado de privação … e ao invés disso ele retornar ao
estado humano, então onde quer que ele nasça, será tolo. Esse é o modo,
estudante, que conduz à tolice, isto é, a pessoa não visita um contemplativo ou
brâmane e não faz aquelas perguntas.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18.
&quot;Mas neste caso, estudante, um homem ou mulher ao visitar um contemplativo
ou brâmane, pergunta: 'Venerável senhor, o que é benéfico? … Que tipo de ação
causará o meu bem-estar e felicidade por muito tempo?' Porque ele realiza e se
empenha em tal ação … ele renasce num destino feliz … e ao invés disso ele retornar
ao estado humano, então onde quer que ele nasça, será sábio. Esse é o modo,
estudante, que conduz à sabedoria, isto é, a pessoa visita um contemplativo ou
brâmane e faz aquelas perguntas..<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>19.
&quot;Portanto, estudante, o modo que conduz à vida curta faz com que as
pessoas tenham vida curta, o modo que conduz à vida longa faz com que as
pessoas tenham vida longa; o modo que conduz à falta de saúde faz com que as
pessoas sejam doentias, o modo que conduz à saúde faz com que as pessoas sejam
saudáveis; o modo que conduz à feiúra faz com que as pessoas sejam feias, o
modo que conduz à beleza faz com que as pessoas sejam belas; o modo que
conduz à falta de poder faz com que as pessoas não tenham poder, o modo que
conduz ao poder faz com que as pessoas tenham poder; o modo que conduz à
pobreza faz com que as pessoas sejam pobres, o modo que conduz à riqueza faz
com que as pessoas sejam ricas; o modo que conduz ao nascimento numa classe
inferior faz com que as pessoas nasçam numa classe inferior, o modo que conduz
ao nascimento numa classe superior faz com que as pessoas nasçam numa classe
superior; o modo que conduz à tolice faz com que as pessoas sejam tolas, o
modo que conduz à sabedoria faz com que as pessoas sejam sábias. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>20.
&quot;Os seres são os donos das suas ações, estudante, herdeiros das suas
ações, nascem das suas ações, estão atados às suas ações, possuem as suas ações
como refúgio. É a ação que distingue os seres entre inferiores ou
superiores.&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>21. Quando
isso foi dito, o estudante brâmane Subha, filho de Todeyya, disse para o
Abençoado: &quot;Magnífico , Mestre Gotama! Magnífico, Mestre Gotama! Mestre
Gotama esclareceu o Dhamma de várias formas, como se tivesse colocado em pé o
que estava de cabeça para baixo, revelasse o que estava escondido, mostrasse o
caminho para alguém que estivesse perdido ou segurasse uma lâmpada no escuro
para aqueles que possuem visão pudessem ver as formas. Eu busco refúgio no
Mestre Gotama, no Dhamma e na Sangha dos bhikkhus. Que o Mestre Gotama me aceite como um discípulo leigo que buscou refúgio para o resto da vida.&quot;<o:p></o:p></span></p>

