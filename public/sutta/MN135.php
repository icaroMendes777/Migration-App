<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[2] Se o kamma de matar determina diretamente o modo de renascimento,
este irá produzir o renascimento em um dos estados de privação. Mas se um kamma
benéfico produz um renascimento humano - e o renascimento como ser humano
sempre é resultado de kamma benéfico - o kamma de matar irá operar de uma
maneira contrária àquele do kamma que gerou o renascimento, causando várias
adversidades que podem culminar em uma morte prematura. O mesmo princípio se
aplica para os casos subseqüentes em que o kamma prejudicial matura em uma
existência humana: em todos os casos o kamma prejudicial age em sentido
contrário ao kamma benéfico responsável pelo nascimento humano, engendrando um
tipo específico de infortúnio correspondente à sua qualidade distintiva. [<a
