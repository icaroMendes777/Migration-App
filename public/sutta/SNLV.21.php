desgarrado. Nessa ocasião, venerável senhor, a minha atenção plena em relação
ao Abençoado fica confusa, a minha atenção plena em relação ao Dhamma fica
confusa, a minha atenção plena em relação à Sangha fica confusa. O pensamento
então me ocorre: ‘Se neste momento eu morresse, qual seria o meu destino, qual
seria o meu futuro percurso?’”<o:p></o:p></span></p>



