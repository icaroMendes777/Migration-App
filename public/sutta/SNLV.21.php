será ruim. Quando a mente de uma pessoa foi fortalecida durante muito tempo pela convicção, virtude, aprendizado, generosidade, e sabedoria, aqui mesmo corvos, abutres, falcões, cachorros e chacais, ou várias criaturas devoram esse corpo, que consiste de forma, que consiste dos quatro grandes elementos, procriado por uma mãe e um pai, construído com arroz cozido e mingau, sujeito à impermanência, a ser gasto e pulverizado, à dissolução e desintegração. Mas a sua mente que foi fortalecida durante muito tempo pela convicção, virtude, aprendizado, generosidade, e sabedoria - esta vai para cima, vai para a distinção. [<a
