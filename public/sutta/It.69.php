<p class=Normal><a name=R1></a> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: “Bhikkhus, qualquer bhikkhu ou bhikkhuni em quem o apego não foi abandonado, a raiva não foi abandonada, e a delusão não foi abandonada, é dito ser alguém que não cruzou o oceano com as suas ondas, crocodilos, redemoinhos, com os seus tubarões. [<a href="#N1">1</a>]</p>

<p class=Normal> “Mas qualquer bhikkhu ou bhikkhuni em quem o apego foi abandonado, a raiva foi abandonada, e a delusão foi abandonada, é dito ser alguém que cruzou o oceano com as suas ondas, crocodilos, redemoinhos, com os seus tubarões."</p>

<p class=Normal style='margin-left:.75in'> Quem destruiu os apegos<br>
junto com a raiva e a delusão, <br>
cruzou este oceano<br>
com os seus crocodilos e tubarões, <br>
as suas temíveis ondas e redemoinhos tão difíceis de vencer.</p>

<p class=Normal style='margin-left:.75in'>Superando tudo, <br>
deixando a morte para trás, <br>
estando livre do apego, <br>
abandonando o sofrimento e a renovação do ser/existir. <br>
Sumido, ele não pode ser definido, eu digo -<br>
ele desnorteou o Senhor da Morte.</p>


