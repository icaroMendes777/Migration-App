<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
se os errantes de outras seitas perguntarem-lhes: “Com qual propósito, amigos,
a vida santa é vivida sob o contemplativo Gotama?’ – sendo assim perguntados,
vocês deveriam responder a esses errantes da seguinte forma: ‘Amigos, é para a
completa compreensão do sofrimento que a vida santa é vivida sob o Abençoado.’
Então, bhikkhus, se aqueles errantes perguntarem a vocês: ‘Amigos, qual é esse
sofrimento para cuja completa compreensão a vida santa é vivida sob o
contemplativo Gotama?’ – sendo assim perguntados, vocês deveriam responder a
esses errantes da seguinte forma:<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’O olho,
amigos, é sofrimento: é para a completa compreensão disso que a vida santa é
vivida sob o Abençoado. As formas são sofrimento: é para a completa compreensão
delas que a vida santa é vivida sob o Abençoado. A consciência no olho é
sofrimento ... o contato no olho é sofrimento ... Qualquer sensação que surja
tendo o contato no olho como condição – quer seja prazerosa, dolorosa ou nem
prazerosa, nem dolorosa – isso também é sofrimento: é para a completa
compreensão disso que a vida santa é vivida sob o Abençoado. O ouvido é
sofrimento ... A mente é sofrimento ... Os objetos mentais são sofrimento ... 
A consciência na mente é
sofrimento ... O contato na mente é sofrimento ... Qualquer sensação que surja tendo o
contato na mente como condição – quer seja prazerosa, dolorosa ou nem prazerosa,
nem dolorosa – isso também é sofrimento: é para a completa compreensão disso
que a vida santa é vivida sob o Abençoado.’<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sendo
