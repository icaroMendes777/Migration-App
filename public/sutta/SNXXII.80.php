<p class=Normal>Em certa ocasião o Abençoado estava entre os Sakyas, em Kapilavatthu, no Parque de Nigrodha. Então, depois de ter dispensado os bhikkhus devido a uma razão em particular, [<a href="#N1">1</a>] <a name="R1"> </a>ao amanhecer, o Abençoado se vestiu e tomando a tigela e o manto externo foi para Kapilavatthu para esmolar alimentos. Depois de haver esmolado em Kapilavatthu e de haver retornado, após a refeição ele foi até a Grande Floresta para passar o resto do dia. Tendo entrado no bosque, ele se sentou ao pé de uma marmeleira-da-índia. </p>

<p class=Normal>Então, estando só em isolamento, o seguinte pensamento surgiu na mente do 
Abençoado: [<a href="#N2">2</a>] <a name="R2"> </a> “A Sangha dos bhikkhus foi dispensada 
por mim. Aqui há bhikkhus novos, que acabam de entrar para a vida santa, que recém-chegaram 
neste Dhamma e Disciplina. Se eles não tiverem a oportunidade de me ver, poderá ocorrer neles 
alguma mudança ou alteração. [<a href="#N3">3</a>] <a name="R3"> </a> Tal como quando um bezerro tenro não vê a mãe poderá 
nele ocorrer alguma mudança ou alteração, assim também, há bhikkhus novos, que acabam de entrar para a vida santa, 
que recém-chegaram neste Dhamma e Disciplina. Se eles não tiverem a oportunidade de me ver, poderá ocorrer neles 
alguma mudança ou alteração. Tal como quando mudas tenras não obtêm água, poderá nelas ocorrer alguma mudança 
ou alteração, assim também, há bhikkhus novos, que acabam de entrar para a vida santa, que recém-chegaram 
neste Dhamma e Disciplina. Se eles não tiverem a oportunidade de me ver, poderá ocorrer neles alguma mudança ou 
alteração. Que eu agora demonstre compaixão para com a Sangha dos bhikkhus, como costumava demonstrar no passado.”

<p class=Normal>Então o Brahma Sahampati soube com a sua mente o pensamento na mente do Abençoado, com a mesma rapidez com que um homem forte pode estender o seu braço flexionado ou flexionar o seu braço estendido, Brahma Sahampati desapareceu do mundo de Brahma e apareceu na frente do Abençoado. Ele arrumou o seu manto externo sobre o ombro e juntou as mãos numa reverenciosa saudação, dizendo: 

<p class=Normal> “Assim é, Abençoado! Assim é, Iluminado! A Sangha dos bhikkhus foi dispensada pelo Abençoado. 
Aqui há bhikkhus novos, que acabam de entrar para a vida santa ... (igual acima, incluindo os símiles) ... se eles 
não tiverem a oportunidade de ver o Abençoado, poderá ocorrer neles alguma mudança ou alteração. Venerável senhor, 
que o Abençoado se deleite com a Sangha dos bhikkhus; venerável senhor, que o Abençoado dê as boas vindas à Sangha 
dos bhikkhus; venerável senhor, que o Abençoado agora demonstre compaixão para com a Sangha dos bhikkhus, como ele 
costumava demonstrar no passado.”

<p class=Normal>O Abençoado concordou em silêncio. Então, sabendo que o Abençoado havia concordado, o Brahma Sahampati levantou do seu assento, e depois de homenagear o Abençoado, mantendo-o à sua direita, partiu. 

<p class=Normal>Então depois de anoitecer o Abençoado saiu do seu isolamento e foi até o Parque de  Nigrodha. Ele sentou num assento que havia sido preparado e realizou tamanha façanha de modo que os bhikkhus viessem tímidos, sós e em pares. [<a href="#N4">4</a>] <a name="R4"> </a> Então aqueles bhikkhus foram até o Abençoado, tímidos, sós e em pares, e depois de cumprimentá-lo sentaram a um lado. O Abençoado disse:

<p class=Normal> “Bhikkhus, este é o modo de vida mais baixo, isto é, esmolar comida. No mundo 
esse é um termo ofensivo: ‘Você é um pedinte de comida, você anda por aí com uma tigela de mendicante 
nas mãos!’ E no entanto, bhikkhus, membros de um clã que intencionam o bem, adotam esse modo de vida 
com base num motivo justo. Não é porque eles foram forçados por reis que eles fazem isso, nem porque 
foram forçados por ladrões, nem porque têm dívidas, ou por medo, e tampouco para obter sustento. Mas eles 
assim fazem com o pensamento: ‘Eu sou uma vítima do nascimento, envelhecimento e morte, da tristeza, 
lamentação, dor, angústia e desespero; eu sou uma vítima do sofrimento, uma presa do sofrimento. 
Com certeza, um fim a toda essa massa de sofrimento pode ser apreendido.’

<p class=Normal> “É desse modo, bhikkhus, que um membro de um clã segue a vida santa. No entanto, ele é cobiçoso, inflamado pela cobiça pelos prazeres sensuais, com a mente cheia de má vontade, com as intenções corrompidas pela raiva, com a mente confusa, sem plena consciência, desconcentrado, com a mente dispersa, com as faculdades dos sentidos descontroladas. Tal como um pedaço de lenha de uma pira funerária, ardendo em ambas pontas e suja de excremento no meio, não pode ser usado como madeira no vilarejo ou na floresta, da mesma forma eu falo dessa pessoa: ele perdeu os prazeres de um chefe de família sem no entanto preencher os requisitos do ascetismo. 

<p class=Normal> “Há, bhikkhus, três tipos de pensamentos ruins e prejudiciais: pensamentos de sensualidade, 
pensamentos de má vontade, pensamentos de crueldade. E onde, bhikkhus, esses três tipos de pensamentos ruins 
e prejudiciais cessam sem deixar vestígio? Naquele que permanece com a mente bem estabelecida nos quatro fundamentos da atenção plena, ou naquele que desenvolve a concentração sem sinais da mente. [<a href="#N5">5</a>] <a name="R5"> </a>Isso é motivo suficiente, bhikkhus, para desenvolver a concentração sem sinais. Quando a concentração sem sinais é desenvolvida e cultivada, bhikkhus, ela produz grandes frutos e benefícios. 

<p class=Normal> “Bhikkhus, há esses dois tipos de idéias: a idéia da existência e a idéia da não existência. 
[<a href="#N6">6</a>] <a name="R6"> </a>Com relação a isso, bhikkhus, o nobre discípulo bem instruído 
reflete assim: ‘Há algo no mundo a que eu poderia me apegar que não seja criticável?’ Ele compreende assim: ‘Não há nada no mundo a que eu possa me apegar que não seja criticável. Pois se eu tiver apego, será apenas à forma que estarei me apegando, apenas à sensação ... apenas à percepção ... apenas às formações volitivas ... apenas à consciência que estarei me apegando. Com esse meu apego como condição, surgiria o ser/existir; com o ser/existir como condição, o nascimento; com o nascimento como condição, o envelhecimento, morte, tristeza lamentação, dor, angústia e desespero surgiriam. Essa seria a origem de toda essa massa de sofrimento. [<a href="#N7">7</a>] 

<p class=Normal> <a name="R7"> </a> “O que vocês pensam, bhikkhus, a forma é permanente ou impermanente? ... a sensação ... a percepção ... as formações volitivas ... a consciência é permanente ou impermanente?”
<p class=Normal> “Impermanente, venerável senhor.” 
<p class=Normal> “E aquilo que é impermanente é sofrimento ou felicidade?”
<p class=Normal> “Sofrimento, venerável senhor.”
<p class=Normal> “E é adequado considerar o que é impermanente, sofrimento, sujeito a mudanças como: 'Isso é meu. Isso sou eu. Isso é o meu eu’?”
<p class=Normal> “Não, venerável senhor.”
<p class=Normal> “Vendo dessa forma, o nobre discípulo bem instruído se desencanta com a forma, se desencanta com a sensação, se desencanta com a percepção, se desencanta com as formações volitivas, se desencanta com a consciência. Desencantado ele se torna desapegado. Através do desapego a sua mente é libertada. Quando ela está libertada surge o conhecimento: ‘Libertada.’ Ele compreende que: ‘O nascimento foi destruído, a vida santa foi vivida, o que deveria ser feito foi feito, não há mais vir a ser a nenhum estado.’”

