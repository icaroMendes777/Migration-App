<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. Agora, naquela ocasião o jovem deva Suriya havia sido seqüestrado por
Rahu, o senhor dos asuras. [<a href="#N1">1</a>] <a name=R1></a>Então,
recordando-se do Abençoado, o jovem deva Suriya recitou o seguinte verso:<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-tab-count:1'>            </span>“Presto homenagem ao Buda! <br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-tab-count:1'>            </span>Oh herói! Você está completamente
libertado.<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-tab-count:1'>            </span>Eu estou cativo,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-tab-count:1'>            </span>por favor seja o meu refúgio.”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então,
referindo-se ao jovem deva Suriya, o Abençoado se dirigiu em versos a Rahu, o
senhor dos asuras:<span style='mso-tab-count:1'>        </span><o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-tab-count:1'>            </span>“Suriya buscou refúgio<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-tab-count:1'>            </span>no Tathagata, o Arahant.<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-tab-count:1'>            </span>Liberte Suriya, Oh! Rahu,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-tab-count:1'>            </span>os Budas têm compaixão pelo mundo.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-tab-count:1'>            </span>“Enquanto você se move através do
céu, Oh! Rahu,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-tab-count:1'>            </span>não engula o radiante,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-tab-count:1'>            </span>aquele que produz a luz na
escuridão,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-tab-count:1'>            </span>o disco com incandescente poder nas
trevas.<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-tab-count:1'>            </span>Rahu, liberte o meu filho Suriya.” [<a
href="#N2">2</a>]<o:p></o:p></span></p>


<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Então Rahu, o senhor dos asuras, libertou o jovem deva Suriya e
apressado foi até Vepacitti, o senhor dos asuras. Ao chegar, chocado e
aterrorizado, ele ficou em pé a um lado. Então, como ele ali permanecesse,
Vepacitti, o senhor dos asuras, se dirigiu a ele em versos:<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-tab-count:1'>            </span>“Porque, Rahu, você veio tão
apressado?<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-tab-count:1'>            </span>Porque você libertou Suriya?<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-tab-count:1'>            </span>Chegando como que em choque,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-tab-count:1'>            </span>porque você está aí parado com
medo?”<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-tab-count:1'>            </span>“Minha cabeça teria se partido em
sete,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-tab-count:1'>            </span>enquanto vivesse eu não teria paz,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-tab-count:1'>            </span>se, ao ouvir os versos do Buda,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-tab-count:1'>            </span>eu não tivesse libertado Suriya.”<span
style='mso-tab-count:1'>           </span><o:p></o:p></span></p>


