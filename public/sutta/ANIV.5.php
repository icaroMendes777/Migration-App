<p class=Normal> 
“Esses quatro tipos de pessoas são encontrados no mundo. Quais quatro?
A pessoa que vai com a torrente, a pessoa que vai contra a torrente, a pessoa que
permanece firme no mesmo lugar e aquela que cruzou até a outra margem, que
atravessou, que pisa sobre terra firme: um brâmane.<o:p></o:p></span></p>


<p class=Normal>
“E quem é a pessoa que vai com a torrente? É o caso em que a pessoa se
entrega aos prazeres dos sentidos e comete atos ruins e prejudicais. Essa é chamada
a pessoa que vai com a torrente. <o:p></o:p></span></p>


<p class=Normal>
“E quem é a pessoa que vai contra a torrente? É o caso em que a pessoa
não se entrega aos prazeres dos sentidos e não comete atos ruins e prejudicais.
Mesmo que ela sinta dor, mesmo que ela sinta tristeza, mesmo que ela esteja
chorando com a face coberta de lágrimas, ela vive a vida santa que é perfeita e
pura. Essa é chamada a pessoa que vai contra a torrente. <o:p></o:p></span></p>


<p class=Normal>
“E quem é a pessoa que permanece frirme no mesmo lugar? É o caso em que
a pessoa, com a destruição dos primeiros cinco grilhões, irá renascer
espontaneamente [nas Moradas Puras] e lá irá realizar o parinibbana sem nunca
mais retornar daquele mundo. Essa é chamada a pessoa que permanece firme no
mesmo lugar<o:p></o:p></span></p>


<p class=Normal>
“E quem é a pessoa que cruzou até a outra margem, que atravessou, que
pisa sobre terra firme: um brâmane? É o caso em que a pessoa, com a eliminação
das impurezas mentais <i style='mso-bidi-font-style:normal'>(asava)</i>,
permanece em um estado livre de impurezas com a libertação da mente e a
libertação através da sabedoria, tendo conhecido e manifestado isso para si mesma no
aqui e agora. Essa é chamada a pessoa que cruzou até a outra margem, que
atravessou, que pisa sobre terra firme: um brâmane.<o:p></o:p></span></p>


<p class=Normal>
“ Essas são os quatro tipos de pessoas que são encontrados no mundo.”<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR>Pessoas irrefreáveis<br>
nos prazeres dos sentidos,<br>
não desprovidas <br>
de paixão,<br>
cedendo ao desejo <br>
dos prazeres dos sentidos:<br>
elas retornam ao nascimento e envelhecimento,<br>
novamente e novamente -- <br>
tomadas pelo desejos,<br>
indo com a torrente. </span></p>

<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR>Dessa forma o iluminado,<br>
com a atenção plena aqui estabelecida,<br>
não cedendo ao desejo<br>
dos prazeres dos sentidos e daquilo que é ruim e prejudicial,<br>
ainda que sinta dor,<br>
abandona os prazeres dos sentidos.<br>
Eles o chamam<br>
aquele que vai contra<span style='mso-spacerun:yes'>  </span><br>
a torrente. </span></p>

<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR>Quem, <br>
tendo abandonado <br>
os cinco grilhões,<br>
é<br>
perfeito na prática, <br>
não destinado a decair,<br>
habilidoso na atenção,<br>
com os sentidos contidos:<br>
ele é chamado<br>
aquele que resiste e permanece no mesmo lugar </span></p>

<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR>Naquele que, tendo compreendido,<br>
as qualidades superiores e inferiores<br>
foram destruídas,<br>
tiveram o seu fim,<br>
não existem:<br>
Ele é chamado<br>
um mestre no conhecimento,<br>
aquele que realizou a vida santa,<br>
chegou ao fim do mundo, foi além</span></p>

