<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, se os errantes de outras seitas perguntarem para vocês:
‘Por qual propósito, amigos, a vida santa é vivida sob o contemplativo Gotama?’
– sendo assim preguntados, vocês deveriam responder desta forma: ‘É, amigos,
para o desaparecimento da cobiça que a vida santa é vivida sob o Abençoado.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
bhikkhus, se os errantes de outras seitas perguntarem para vocês: ‘Mas, amigos,
há um caminho, há um meio para o desaparecimento da cobiça?’ – sendo assim perguntados
vocês deveriam responder desta forma: ‘Há um caminho, amigos, há um meio para o
desaparecimento da cobiça.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é esse caminho, qual é esse meio para o desaparecimento da cobiça? É
este Nobre Caminho Óctuplo; isto é, entendimento correto ... concentração
correta. Esse é o caminho, esse é o meio para o desaparecimento da cobiça.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sendo
