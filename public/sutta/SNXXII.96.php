<p class=Normal> Em Savatthi. Sentado a um lado, um certo bhikkhu disse para o Abençoado: “Senhor, existe alguma forma que seja permanente, estável, eterna, não sujeita à mudança, que permanecerá tal como é por tanto tempo quanto a eternidade? Existe alguma sensação ... alguma percepção ... alguma formação ...  Existe alguma consciência que seja permanente, estável, eterna, não sujeita à mudança, que permanecerá tal como é por tanto tempo quanto a eternidade? </p>

<p class=Normal> “Não, bhikkhu, não há forma ... não há sensação ... não há percepção ... não há formação ... não há consciência que seja permanente, estável, eterna, não sujeita à mudança e que permanecerá tal como é por tanto tempo quanto a eternidade.” [<a href="#N1">1</a>]</p>

<p class=Normal><a name=R1></a> Então o Abençoado tomando uma pequeno torrão de esterco de vaca, disse para o bhikkhu, “Não existe nem este tanto de individualidade que seja permanente, estável, eterna, não sujeita à mudança, que irá permanecer tal como é por tanto tempo quanto a eternidade. Se houvesse nem que fosse este tanto de individualidade que fosse permanente, estável, eterna, não sujeita à mudança, que permanecesse tal como é por tanto tempo quanto a eternidade, então viver esta vida santa para a completa destruição do sofrimento não seria discernido. Mas como não existe nem esse tanto de individualidade que seja permanente, estável, eterna, não sujeita à mudança, que permaneça tal como é por tanto tempo quanto a eternidade, viver esta vida santa para o correto fim do sofrimento é discernido. </p>

<p class=Normal> "No passado, bhikkhu, eu fui um rei khaittiya ungido. Eu tinha 84.000 cidades, a principal das quais era Kusavati. Eu tinha 84.000 palácios, o principal dos quais era o palácio [chamado] Dhamma. Eu tinha 84.000 salões com cumeeira, o principal dos quais era o grande salão. Eu tinha 84.000 sofás feitos de marfim, sândalo, de ouro e prata, cobertos com colchas felpudas, coberta com colchas de lã branca, colchas bordadas, peles de antílope e gamo, coberta com um baldaquino e com almofadas vermelhas para a cabeça e os pés. </p>

<p class=Normal> "Eu tinha 84.000 elefantes adornados com ornamentos de ouro, com estandartes de ouro e cobertos com redes de ouro, o principal  dos quais era o elefante real Uposatha; eu tinha 84.000 carruagens cobertas com peles de leão, peles de tigre, peles de leopardos ou com tecidos alaranjados adornados com ornamentos de ouro, com estandartes de ouro e cobertas com redes de ouro, das quais a principal era a carruagem [chamada] Vejayanta. </p>

<p class=Normal> "Eu tinha 84.000 jóias sendo que a principal  era a jóia preciosa; eu tinha 84.000 esposas sendo que a principal era a Rainha Subhadda; eu tinha 84.000 chefes de família sendo que o principal era o chefe de família precioso; eu tinha 84.000 empregados Khattiya sendo que o principal era conselheiro precioso; eu tinha 84.000 vacas com ubres como fina juta e baldes de prata; eu tinha 84.000 fardos de roupas feitas do mais fino linho, algodão, seda e lã; eu tinha 84.000 oferendas de arroz que ali estavam para alimentar os necessitados, noite e dia. </p>

<p class=Normal> "Dessas 84.000 cidades, bhikkhu, naquela época havia apenas uma cidade na qual eu residia: a capital Kusavati. Daqueles 84.000 palácios, naquela época havia apenas um palácio no qual eu residia: o palácio [chamado] Dhamma. Daqueles 84.000 salões com cumeeira, naquela época havia apenas um salão com cumeeira no qual eu residia: o grande salão. Daqueles 84.000 sofás, naquela época havia apenas um sofá que eu usava: o sofá feito de marfim, de sândalo, de ouro ou de prata. </p>

<p class=Normal> "Daqueles 84.000 elefantes, naquela época havia apenas um elefante que eu montava: o elefante real chamado Uposatha. Daquelas 84.000 carruagens, naquela época havia apenas uma carruagem que eu montava: a carruagem chamada Vejayanta. </p>

<p class=Normal> "Daquelas 84.000 mulheres, naquela época havia apenas uma mulher na minha companhia, uma donzela khattiya ou uma donzela velamika. Daqueles 84.000 fardos de roupas finas, naquela época havia apenas um par de roupas que eu vestia, feito de linho, ou de fina seda, ou de fina lã, ou de fino algodão. Daquelas 84.000 oferendas de arroz, naquela época havia apenas uma que eu comia com uma medida de arroz e caril.

<p class=Normal> "Portanto, bhikkhu, todas essas formações deixaram de ser, cessaram, mudaram. Assim impermanentes são as formações, bhikkhu, tão instáveis, pouco confiáveis. Isso é o suficiente, bhikkhu, para o desencantamento das formações, suficiente  para o desapego das formações, suficiente para se libertar das formações."</p>


