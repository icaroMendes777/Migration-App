<p class=Normal> "Bhikkhus, o Tathagata possui esses seis poderes de um Tathagata, possuindo-os ele reivindica o lugar de líder do rebanho, ruge o seu rugido de leão nas assembléias e coloca em movimento a roda de Brahma. Quais seis? </p>

<p class=Normal> (1) "Aqui, o Tathagata compreende como na verdade é, o possível como possível e o impossível como impossível. E esse é um dos poderes dos Tathagatas que o Tathagata possui, pelo qual ele reivindica o lugar de líder do rebanho, ruge o seu rugido de leão nas assembléias e coloca em movimento a roda de Brahma. </p>

<p class=Normal> (2) "Outra vez, o Tathagata compreende como na verdade é, os resultados das ações praticadas, passadas, futuras e presentes, com as possibilidades e com as causas. Esse também é um dos poderes dos Tathagatas …</p>

<p class=Normal> (3) "Outra vez, o Tathagata compreende como na verdade é, as impurezas, a purificação, o surgimento dos jhanas, as libertações, as concentrações e as realizações. Esse também é um dos poderes dos Tathagatas …</p>

<p class=Normal> (4) "Outra vez, o Tathagata se recorda das suas muitas vidas passadas, isto é, um nascimento, dois nascimentos … (veja o <a href="MN4.php#T27">MN 4 verso 27</a> ) … Assim ele se recorda das suas muitas vidas passadas nos seus modos e detalhes. Esse também é um dos poderes dos Tathagatas …</p>

<p class=Normal> (5) "Outra vez, com o olho divino que é purificado e sobrepuja o humano, o Tathagata vê seres falecendo e renascendo, inferiores e superiores, bonitos e feios, afortunados e desafortunados … (veja o <a href="MN4.php#T29">MN 4 verso 29</a> ) … e ele compreende como os seres continuam de acordo com as suas ações. Esse também é um dos poderes dos Tathagatas …</p>

<p class=Normal> (6) "Outra vez, realizando por si mesmo com conhecimento direto, o Tathagata aqui e agora entra e permanece na libertação da mente e libertação através da sabedoria que são imaculadas, com a destruição de todas as impurezas. Esse também é um dos poderes dos Tathagatas que o Tathagata possui, pelo qual ele reivindica o lugar de líder do rebanho, ruge o seu rugido de leão nas assembléias e coloca em movimento a roda de Brahma. </p>

<p class=Normal>"O Tathagata possui esses seis poderes de um Tathagata, possuindo-os ele reivindica o lugar de líder do rebanho, ruge o seu rugido de leão nas assembléias e coloca em movimento a roda de Brahma. </p>

<p class=Normal> (1) "Bhikkhus, se outros forem até o Tathagata e perguntarem algo relacionado ao seu conhecimento como na verdade é, sobre o possível como possível e o impossível como impossível, então, o Tathagata questionado dessa forma, responde exatamente de acordo com o conhecimento que ele obteve. </p>

<p class=Normal> (2) "Bhikkhus, se outros forem até o Tathagata e perguntarem algo relacionado ao seu conhecimento como na verdade é, dos resultados das ações praticadas, passadas, futuras e presentes, com as possibilidades e com as causas, então, o Tathagata questionado dessa forma, responde exatamente de acordo com o conhecimento que ele obteve. </p>

<p class=Normal> (3) "Bhikkhus, se outros forem até o Tathagata e perguntarem algo relacionado ao seu conhecimento como na verdade é, sobre as impurezas, a purificação, o surgimento dos jhanas, as libertações, as concentrações e as realizações, então, o Tathagata questionado dessa forma, responde exatamente de acordo com o conhecimento que ele obteve. </p>

<p class=Normal> (4) "Bhikkhus, se outros forem até o Tathagata e perguntarem algo relacionado ao seu conhecimento como na verdade é, sobre a recordação das muitas vidas passadas, então, o Tathagata questionado dessa forma, responde exatamente de acordo com o conhecimento que ele obteve. </p>

<p class=Normal> (5) "Bhikkhus, se outros forem até o Tathagata e perguntarem algo relacionado ao seu conhecimento como na verdade é, sobre o falecimento e renascimento dos seres, então, o Tathagata questionado dessa forma, responde exatamente de acordo com o conhecimento que ele obteve. </p>

<p class=Normal> (6) "Bhikkhus, se outros forem até o Tathagata e perguntarem algo relacionado ao seu conhecimento como na verdade é, sobre a libertação da mente e libertação através da sabedoria que são imaculadas, então, o Tathagata questionado dessa forma, responde exatamente de acordo com o conhecimento que ele obteve. </p>

<p class=Normal> (1) "Eu digo, bhikkhus, que o conhecimento como na verdade é, sobre o possível como possível e o impossível como impossível ocorre naquele cuja mente está controlada, não descontrolada. [<a href="#N1">1</a>]</p>

<p class=Normal><a name=R1></a> (2) "Eu digo, bhikkhus, que o conhecimento como na verdade é, dos resultados das ações praticadas, passadas, futuras e presentes, com as possibilidades e com as causas, ocorre naquele cuja mente está controlada, não descontrolada. </p>

<p class=Normal> (3) "Eu digo, bhikkhus, que o conhecimento como na verdade é, sobre a impureza, a purificação, o surgimento dos jhanas, as libertações, as concentrações e as realizações, ocorre naquele cuja mente está controlada, não descontrolada. </p>

<p class=Normal> (4) "Eu digo, bhikkhus, que o conhecimento como na verdade é, sobre a recordação das muitas vidas passadas, ocorre naquele cuja mente está controlada, não descontrolada. </p>

<p class=Normal> (5) "Eu digo, bhikkhus, que o conhecimento como na verdade é, sobre o falecimento e renascimento dos seres, ocorre naquele cuja mente está controlada, não descontrolada. </p>

<p class=Normal> (6) "Eu digo, bhikkhus, que o conhecimento como na verdade é, sobre a libertação da mente e libertação através da sabedoria que são imaculadas, ocorre naquele cuja mente está controlada, não descontrolada. </p> 

<p class=Normal>"Portanto, bhikkhus, a concentração é o caminho; a falta de concentração é o caminho errado." [<a href="#N2">2</a>]</p>


