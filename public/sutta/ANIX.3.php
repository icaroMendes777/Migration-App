<p class=Normal style='margin-left:.75in'> 

<p class=Normal>Assim ouvi. Em certa ocasião, o Abençoado estava em Calika no Monte Calika. Agora, naquela ocasião o venerável Meghiya era o atendente do Abençoado. Então o venerável Meghiya foi até o Abençoado e depois de cumprimentá-lo ficou em pé a um lado e disse: "Venerável senhor, eu gostaria ir até Jantugama para esmolar alimentos." </p>

<p class=Normal>"Você pode fazer isso, Meghiya, quando for apropriado." </p>

<p class=Normal><a name=R1></a>  Então, pela manhã, o venerável Meghiya se vestiu e tomando a tigela e o manto externo, foi para Jantugama para esmolar alimentos. Depois de haver esmolado em Jantugama e de haver retornado, após a refeição, ele foi até a margem do rio Kimikala. Enquanto caminhava fazendo exercício ao longo da margem do rio Kimikala, o venerável Meghiya viu um manguezal adorável e agradável. Ocorreu-lhe: "Este manguezal é realmente adorável e agradável, adequado para o esforço [<a href="#N1">1</a>]  pelo membro de um clã que quer se dedicar ao esforço. Se o Abençoado me permitir, retornarei a este manguezal para me esforçar." </p>

<p class=Normal>Então o venerável Meghiya foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse: "Esta manhã, venerável senhor, me vesti e tomei a tigela e o manto externo e fui .... [como acima, mas na primeira pessoa.] ... Eu pensei: 'Este manguezal é realmente adorável e agradável, adequado para o esforço pelo membro de um clã que quer se dedicar ao esforço. Se o Abençoado me permitir, retornarei a este manguezal para me esforçar.' Então, se o Abençoado me permitir, retornarei para aquele manguezal para me esforçar." </p>

<p class=Normal>"Como estamos sozinhos, Meghiya, espere até que algum outro bhikkhu venha." </p>

<p class=Normal>Uma segunda vez o Venerável Meghiya disse para o Abençoado: "Venerável senhor, para o Abençoado não há mais nada a ser feito e não [é necessário] aumentar o que já foi feito. Mas, venerável senhor, eu tenho algo mais a ser feito e [necessito] aumentar o que já foi feito. Se o Abençoado me permitir, retornarei para aquele manguezal para me esforçar." </p>

<p class=Normal>"Como estamos sozinhos, Meghiya, espere até que algum outro bhikkhu venha." </p>

<p class=Normal>Uma terceira vez o Venerável Meghiya disse para o Abençoado: "Venerável senhor, para o Abençoado não há mais nada a ser feito e não [é necessário] aumentar o que já foi feito. Mas, venerável senhor, eu tenho algo mais a ser feito e [necessito] aumentar o que já foi feito. Se o Abençoado me permitir, retornarei para aquele manguezal para me esforçar." </p>

<p class=Normal>"Visto que você fala de esforço, Meghiya, o que posso lhe dizer? Você pode ir quando for apropriado." </p>

<p class=Normal>Em seguida, o venerável Meghiya levantou do seu assento e depois de homenagear o Abençoado, mantendo-o à sua direita, partiu, indo para o manguezal. Ele entrou e sentou-se ao pé de uma árvore para passar o dia. Então, enquanto o venerável Meghiya estava naquele manguezal, três tipos de pensamentos ruins e prejudiciais freqüentemente lhe ocorreram: pensamentos de desejo sensual, pensamentos de má vontade, e pensamentos de crueldade. Em seguida lhe ocorreu: "Isto é verdadeiramente espantoso e incrível! Eu deixei a vida em família pela vida santa com base na fé, no entanto ainda sou perseguido por esses três tipos de pensamentos ruins e prejudiciais: pensamentos de desejo sensual, pensamentos de má vontade, e pensamentos de crueldade".</p>

<p class=Normal>Então, o venerável Meghiya foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse: "Aqui, venerável senhor, enquanto eu estava no manguezal, três tipos de pensamentos ruins e prejudiciais frequentemente me ocorreram: pensamentos de desejo sensual, pensamentos de má vontade, e pensamentos de crueldade. Em seguida, me ocorreu: 'Isto é verdadeiramente espantoso e incrível! Eu deixei a vida em família pela vida santa com base na fé, no entanto ainda sou perseguido por esses três tipos de pensamentos ruins e prejudiciais: pensamentos de desejo sensual, pensamentos de má vontade, e pensamentos de crueldade'".</p>

<p class=Normal>"Meghiya, quando a libertação da mente não amadureceu, há cinco coisas que levam à sua maturação. Quais cinco? </p>

<p class=Normal> (1) "Aqui, Meghiya, um bhikkhu tem bons amigos, companheiros e camaradas. Quando a libertação da mente não amadureceu, esta é a primeira coisa que leva à sua maturação. </p>

<p class=Normal> (2) "Outra vez, um bhikkhu é virtuoso, ele permanece contido de acordo com as regras do Patimokkha, perfeito na conduta e na sua esfera de atividades. Temendo a menor falha, ele treina adotando os preceitos de virtude. Quando a libertação da mente não amadureceu, esta é a segunda coisa que leva à sua maturação. </p>

<p class=Normal> (3) "Outra vez, um bhikkhu ouve de acordo com a sua vontade, sem problemas ou dificuldades, conversa sobre o ascetismo que favorece a abertura do coração, isto é, falar sobre: poucos desejos,  contentamento, isolamento, não ficar ligado [com outros], despertar a energia, comportamento virtuoso, concentração, sabedoria, libertação, conhecimento e visão da libertação. Quando a libertação da mente não amadureceu, esta é a terceira coisa que leva à sua maturação.</p>

<p class=Normal> (4) "Outra vez, um bhikkhu permanece
com a sua energia estimulada para abandonar qualidades inábeis e prejudiciais e cultivar qualidades hábeis e benéficas; ele é decidido, firme no seu
esforço, sem se esquivar das suas responsabilidades com relação ao cultivo de
qualidades mentais benéficas. Quando a libertação da mente não amadureceu, esta é a quarta coisa que leva à sua maturação. </p>

<p class=Normal> (5) "Outra vez, um bhikkhu é sábio, ele tem a completa compreensão da origem e
cessação  - nobre, penetrante,
que conduz ao fim do sofrimento. Quando a libertação da mente não amadureceu, esta é a quinta coisa que leva à sua maturação. </p>

<p class=Normal>"Quando, Meghiya, um bhikkhu tem bons amigos, companheiros e camaradas, pode-se esperar que ele será virtuoso, permanecendo contido pelas regras do Patimokkha ...  ele treinará adotando os preceitos de virtude. </p>

<p class=Normal>"Quando um bhikkhu tem bons amigos, companheiros e camaradas, pode-se esperar que ele irá ouvir de acordo com a sua vontade, sem problemas ou dificuldades, conversa sobre o ascetismo que favorece a abertura do coração, isto é, falar sobre: poucos desejos ... conhecimento e visão da libertação. </p>

<p class=Normal>"Quando um bhikkhu tem bons amigos, companheiros e camaradas, pode-se esperar que ele permanecerá com a sua energia estimulada para abandonar qualidades inábeis e prejudiciais ... sem se esquivar das suas responsabilidades com relação ao cultivo de
qualidades mentais benéficas. </p>

<p class=Normal>"Quando um bhikkhu tem bons amigos, companheiros e camaradas, pode-se esperar que ele será sábio, tendo a completa compreensão da origem e
cessação, - nobre, penetrante, que conduz ao fim do sofrimento. </p>

<p class=Normal>"Tendo se baseado nessas cinco coisas, o bhikkhu deve desenvolver ainda mais [outras] quatro coisas. (6) [A percepção da] repulsa deve ser desenvolvida para abandonar a cobiça. (7) O amor bondade deve ser desenvolvido para abandonar a má vontade. (8) A atenção plena na respiração deve ser desenvolvida para eliminar os pensamentos. (9) A percepção da impermanência deve ser desenvolvida para erradicar a presunção 'eu sou.' Quem percebe a impermanência, estabiliza a percepção do não-eu. Quem percebe não-eu erradica a presunção 'eu sou', [que é] nibbana nesta mesma vida." </p>


