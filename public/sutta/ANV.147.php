<p class=Normal> "Bhikkhus, há essas cinco oferendas de uma pessoa falsa. Quais cinco? Ela dá uma oferenda de forma descuidada, não dá com as suas próprias mãos, dá sem demonstrar respeito, dá aquilo que seria jogado fora, dá com a idéia de que nenhum fruto provém dessa ação. Essas são as cinco oferendas de uma pessoa falsa."</p>

<p class=Normal> "Bhikkhus, há essas cinco oferendas de uma pessoa verdadeira. [<a href="#N1">1</a>] Quais cinco? Ela dá uma oferenda de forma cuidadosa, dá com as suas próprias mãos, dá demonstrando respeito, dá aquilo que não seria jogado fora, dá com a idéia de que algum fruto provém dessa ação. [<a href="#N2">2</a>] Essas são as cinco oferendas de uma pessoa verdadeira."</p>

