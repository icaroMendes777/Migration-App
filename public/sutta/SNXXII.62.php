<p class=Normal> Em Savatthi. "Bhikkhus, há esses três meios de linguagem, meios de designação, meios de conceituação, que não se misturam, que nunca estiveram misturados, que não estão sendo misturados, que nunca serão misturados, que não são rejeitados pelos contemplativos e brâmanes. Quais três? </p>


<p class=Normal> "Qualquer forma, bhikkhus, que tenha passado, cessado, mudado: o termo, rótulo e descrição 'foi' é aplicado, não o termo 'é' ou o termo 'será'. </p>


<p class=Normal> "Qualquer sensação, qualquer percepção, qualquer formação volitiva, qualquer consciência, que tenha passado, cessado, mudado: o termo, rótulo e descrição 'foi' é aplicado, não o termo 'é' ou o termo 'será'. </p>


<p class=Normal> "Qualquer forma, bhikkhus, que não tenha nascido, não tenha se manifestado: o termo, rótulo e descrição 'será' é aplicado, não o termo 'é' ou o termo 'foi'. </p>


<p class=Normal> "Qualquer sensação, qualquer percepção, qualquer formação volitiva, qualquer consciência, que não tenha nascido, não tenha se manifestado: o termo, rótulo e descrição 'será' é aplicado, não o termo 'é' ou o termo 'foi'. </p>


<p class=Normal> "Qualquer forma, bhikkhus, que tenha nascido, tenha se manifestado: o termo, rótulo e descrição 'é'  é aplicado, não o termo 'será' ou o termo 'foi'. </p>


<p class=Normal> "Qualquer sensação, qualquer percepção, qualquer formação volitiva, qualquer consciência, que tenha nascido, tenha se manifestado: o termo, rótulo e descrição 'é'  é aplicado, não o termo 'será' ou o termo 'foi'. </p>


<p class=Normal> "Esses, bhikkhus, são os três meios de linguagem, meios de designação, meios de conceituação, que não se misturam, que nunca estiveram misturados, que não estão sendo misturados, que nunca serão misturados, que não são rejeitados pelos contemplativos e brâmanes. </p>


<p class=Normal> "Bhikkhus, até mesmo Vassa e Banna de Ukkala, proponentes da não-causalidade, da ineficácia da ação, do niilismo, não pensavam que esses três meios de linguagem, meios de designação, meios de conceituação deveriam ser criticados ou desprezados. Por qual razão? Porque eles temem a crítica, o ataque, e a condenação."
</p>


