<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião, o Abençoado estava<span style='mso-spacerun:yes'> 
</span>em Vesali na Grande Floresta no Salão com um pico na cumeeira. Agora,
naquela ocasião, um grande número de emissários Brâmanes de Kosala e Magadha
estavam em Vesali para tratar de negócios. E eles ouviram: “Gotama o
contemplativo, o filho dos Sakyas, que adotou a vida santa deixando o clã dos
Sakyas, está em Vesali na Grande Floresta no Salão com um pico na cumeeira. E
acerca desse mestre Gotama existe essa boa reputação: ‘Esse Abençoado é
um arahant, perfeitamente iluminado, consumado no verdadeiro conhecimento e
conduta, bem-aventurado, conhecedor dos mundos, um líder insuperável de pessoas
preparadas para serem treinadas, mestre de devas e humanos, desperto, sublime.
Ele declara - tendo realizado por si próprio com o conhecimento direto - este
mundo com os seus devas, maras e brahmas, esta população com seus contemplativos
e brâmanes, seus príncipes e povo. Ele ensina o Dhamma, com o significado e fraseado
corretos, que é admirável no início, admirável no meio, admirável no final; e
ele revela uma vida santa que é completamente perfeita e imaculada. É bom poder
encontrar alguém tão nobre.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. E assim
aqueles emissários Brâmanes de Kosala e Magadha foram para a Grande Floresta,
para o Salão com um pico na cumeeira. Naquela época o Venerável Nagita era o
acompanhante do Abençoado. Então, eles foram até o Venerável Nagita e disseram:
“Venerável Nagita, onde está agora o Mestre Gotama? Nós queremos ver o Mestre
Gotama.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigos,
não é o momento apropriado para ver o Abençoado. Ele está isolado meditando.”
Mas os Brâmanes sentaram ali mesmo e disseram: “Quando tivermos visto o
Venerável Gotama, iremos embora.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. Quase no
mesmo momento o Licchavi Otthaddha chegou ao Salão com um pico na cumeeira com
uma grande comitiva e depois de cumprimentar o Venerável Nagita ficou em pé a
um lado e disse: “Onde ele está agora, o Abençoado, o arahant, perfeitamente
iluminado?<span style='mso-spacerun:yes'>  </span>Nós queremos vê-lo.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mahali [<a
href="#N1">1</a>] <a name=R1></a>não é o momento apropriado para ver o
Abençoado. Ele está isolado meditando.” Mas Otthaddha sentou ali mesmo e disse:
“Quando eu tiver visto o Abençoado, o arahant, perfeitamente iluminado, irei
embora.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. Então, o
noviço Siha [<a href="#N2">2</a>]<a name=R2></a>foi até o Venerável Nagita,
ficou em pé a um lado, e disse: “Venerável Kassapa [<a href="#N3">3</a>] <a
name=R3></a>estes emissários Brâmanes de Kosala e Magadha vieram até aqui para
ver o Abençoado e o Licchavi Otthaddha também veio com uma grande comitiva para
ver o Abençoado. Seria bom, Venerável Kassapa, permitir que essas pessoas o
vissem.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Muito bem,
então Siha, você os anuncia para o Abençoado.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
Venerável senhor,” disse Siha. Então, ele foi até o Abençoado e depois de
cumprimentá-lo ficou a um lado e disse: “Venerável Senhor, muitos emissários
Brâmanes de Kosala e Magadha vieram até aqui para ver o Abençoado e o<span
style='mso-spacerun:yes'>  </span>Licchavi Otthaddha também veio com uma grande
comitiva para vê-lo. Seria bom, se o Abençoado permitisse que essas pessoas o
vissem.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
Siha, prepare um assento à sombra em frente desta moradia.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
venerável senhor,” Siha disse, e assim fez. Então, o Abençoado saiu da sua
moradia e sentou-se no assento que havia sido preparado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. Os brâmanes
foram até o Abençoado e depois de cumprimentá-lo sentaram a um lado. Mas
Otthaddha fez uma reverência para o Abençoado e depois sentou a um lado e
disse: “Venerável senhor, não faz muito tempo o Licchavi Sunakkhatta [<a
href="#N4">4</a>] <a name=R4></a>veio ter comigo e disse: ‘Em breve irá
completar três anos desde que me tornei um discípulo do Abençoado e<span
style='mso-spacerun:yes'>  </span>sou capaz de ver formas celestiais,
prazerosas, deliciosas, sedutoras, mas eu não ouvi nenhum som celestial que
seja prazeroso, delicioso, sedutor.” Venerável senhor, há esse tipo de som
celestial que Sunakkhatta não é capaz de ouvir ou eles não existem?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Há esse
tipo de som, Mahali.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. “Então,
venerável senhor, qual é a razão, qual é a causa porque<span
style='mso-spacerun:yes'>  </span>Sunakkhatta não pode ouví-los?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mahali, em
um caso, um bhikkhu voltado para o leste pratica uma concentração unilateral e
vê formas celestiais, prazerosas, deliciosas, sedutoras ... mas ele não ouve
sons celestiais. Através dessa concentração unilateral ele vê formas
celestiais, mas não ouve sons celestiais. Por que isso? Porque essa concentração
permite apenas que formas celestiais sejam vistas, mas sons celestiais não são
ouvidos. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7.
“Novamente, um bhikkhu, voltado para o sul, oeste, norte e para cima, para
baixo e de lado a lado, pratica uma concentração unilateral e vê formas
celestiais [naquela direção], mas ele não ouve sons celestiais. Por que isso?
Porque essa concentração unilateral permite apenas que formas celestiais sejam
vistas, mas sons celestiais não são ouvidos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. “Num
outro caso, Mahali, um bhikkhu voltado para o leste ... ouve sons celestiais,
mas não vê formas celestiais ...<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9.
“Novamente, um bhikkhu voltado para o sul, oeste, norte e para cima, para baixo
e de lado a lado ouve sons celestiais, mas não vê formas celestiais ...<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. “Num
outro caso, Mahali, um bhikkhu voltado para o leste pratica uma concentração
bilateral e tanto vê formas celestiais, prazerosas, deliciosas, sedutoras como
ouve sons celestiais, por que isso? Porque essa concentração bilateral permite
ambos,<span style='mso-spacerun:yes'>  </span>ver formas celestiais e ouvir
sons celestiais.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11.
“Novamente, um bhikkhu voltado para o sul, oeste, norte e para cima, para baixo
e de lado a lado ouve sons celestiais e vê formas celestiais ... E essa é a
razão porque<span style='mso-spacerun:yes'>  </span>Sunakkhatta vê formas
celestiais, mas não ouve sons celestiais. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. “Então,
venerável senhor, é com o propósito de alcançar esse tipo de concentração que a
vida santa é vivida sob o Abençoado?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
Mahali, há outras coisas mais elevadas e perfeitas que essas, pelas quais a
vida santa é vivida sob mim.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. “Quais
são elas, venerável senhor?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mahali, em
um caso, um bhikkhu, com a destruição de três grilhões é aquele que entrou na
correnteza, não mais destinado aos mundos inferiores, com o destino fixo, ele
tem a iluminação como destino. Novamente, um bhikkhu, com a destruição de três
grilhões e com a atenuação da cobiça, raiva e delusão, é aquele que retorna
apenas uma vez, retornando a este mundo uma vez para dar um fim ao sofrimento.
Novamente, um bhikkhu, com a destruição dos cinco primeiros grilhões, irá
renascer espontaneamente [nas Moradas Puras] e lá irá realizar o parinibbana sem nunca mais retornar daquele mundo. Novamente, um bhikkhu, é um arahant, com
as impurezas destruídas, ele viveu a vida santa, fez o que devia ser feito,
depôs o fardo, alcançou o objetivo verdadeiro, destruiu os grilhões da
existência e está completamente libertado através do conhecimento supremo. Essa
é outra coisa mais elevada e perfeita do que as anteriores, pelas qual a vida
santa é vivida sob mim.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14.
“Venerável senhor, há um caminho, há um meio para realizar essas coisas?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Há um
caminho, Mahali, há um meio” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, qual é esse caminho, qual é esse meio?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“É
exatamente este Nobre Caminho Óctuplo; isto é, entendimento correto, pensamento
correto, linguagem correta, ação correta, modo de vida correto, esforço
correto, atenção plena correta, concentração correta. Esse é o caminho, esse é
o meio para realizar essas coisas.”<o:p></o:p></span></p>



<p class=Normal><a name=T15></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>15. “Certa vez, Mahali, eu estava em Kosambi, no parque de Ghosita. Lá,
dois contemplativos, Mandissa e Jaliya, pupilos de Darupattika, vieram falar
comigo e depois que nos cumprimentamos eles sentaram a um lado e disseram:
‘Como é isso, amigo Gotama, a alma e o corpo são a mesma coisa ou<span
style='mso-spacerun:yes'>  </span>a alma é uma coisa e o corpo é outra?’ - ‘Muito
bem, amigos, ouçam e prestem muita atenção àquilo que eu vou dizer.’ – ‘Sim,
amigo,’ eles responderam, e eu disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16.
“’Amigos, um Tathagata surge no mundo, um arahant, perfeitamente iluminado,
consumado no verdadeiro conhecimento e conduta, bem-aventurado, conhecedor dos
mundos, um líder insuperável de pessoas preparadas para serem treinadas, mestre
de devas e humanos, desperto, sublime. Ele declara - tendo realizado por si
próprio com o conhecimento direto - este mundo com os seus devas, maras e
brahmas, esta população com seus contemplativos e brâmanes, seus príncipes e o
povo. Ele ensina o Dhamma, com o significado e fraseado corretos, que é
admirável no início, admirável no meio, admirável no final; e ele revela uma
vida santa que é completamente perfeita e imaculada. Um discípulo segue a vida
santa e pratica a virtude (veja o <a href="DN2.php#T41">DN 2, versos 41-62</a>).
Um bhikkhu consumado em virtude, dessa forma, não vê perigo em lugar nenhum
pela sua contenção através da virtude. Dotado desse nobre agregado da virtude,
ele experimenta dentro de si uma felicidade que é imaculada. Assim é como um
bhikkhu é consumado em virtude. (veja o <a href="DN2.php#T64">DN 2, versos
64-74</a>) ... Porém, quando esses cinco obstáculos são abandonados, ele os
considera como não ter dívidas, ter boa saúde, estar livre da prisão, estar
livre, estar num lugar com segurança<span style='mso-spacerun:yes'>  </span>...
afastado dos prazeres sensuais, afastado das qualidades não hábeis, ele entra e
permanece no primeiro jhana ... ele permeia e impregna, cobre e preenche esse
corpo com o êxtase e felicidade nascidos do afastamento. Agora, de alguém que
assim sabe e assim vê é apropriado que se diga: “A alma e o corpo são a mesma
coisa” ou<span style='mso-spacerun:yes'>  </span>“A alma é uma coisa e o corpo
é outra”?’ ‘Não é, amigo.’ [<a href="#N5">5</a>] <a name=R5></a><span
style='mso-spacerun:yes'> </span>‘Mas eu assim sei e vejo, e eu não digo que a
alma e o corpo são a mesma coisa ou que a alma é uma coisa e o corpo é outra.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17. ‘”E da
mesma forma com o segundo ... terceiro e quarto jhana (veja o <a
href="DN2.php#T77">DN 2, versos 77-82</a>).<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18. ‘”A
mente se inclina e tende na direção do conhecimento e visão. Agora de alguém que
assim sabe e vê, é apropriado que se diga: “A alma e o corpo são a mesma coisa”
ou<span style='mso-spacerun:yes'>  </span>“A alma é uma coisa e o corpo é
outra”?’ - ‘Não é, amigo.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>19. “’Ele
sabe: “Não há mais nada neste mundo.”<span style='mso-spacerun:yes'> 
</span>Agora de alguém que assim sabe e vê, é apropriado que se diga: “A alma e
o corpo são a mesma coisa” ou<span style='mso-spacerun:yes'>  </span>“A alma é
uma coisa e o corpo é outra”?’ - ‘Não é, amigo.’ - ‘Mas eu assim sei e vejo, e eu
não digo que a alma e o corpo são a mesma coisa ou que a alma é uma coisa e o
corpo é outra.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o
que disse o Abençoado. O Licchavi Otthaddha ficou satisfeito e contente com as
palavras do Abençoado.<span style='mso-spacerun:yes'>  </span><o:p></o:p></span></p>

