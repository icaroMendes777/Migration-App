<p class=Tit3 align=center style='text-align:center'><b>Samyutta Nikaya XII.51</b></Tit3>
<p class=Tit1 align=center style='text-align:center'>Parivimamsana Sutta </Tit1>
<p class=Tit1 align=center style='text-align:center'>Investigação Profunda </Tit1>

<p class=Normal align=center style='text-align:center'><span lang=PT-BR style='font-size:
