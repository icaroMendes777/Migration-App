<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim ouvi.
Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no Parque
de Anathapindika. Lá ele se dirigiu aos monges desta forma: “Bhikkhus” –
“Venerável Senhor,” eles responderam. O Abençoado disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
quando um bhikkhu está fazendo uma investigação profunda, de que forma ele deve
investigar para a completa e absoluta destruição do sofrimento?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, os nossos ensinamentos têm o Abençoado como origem, como guia e como
refúgio. Seria bom se o Abençoado pudesse explicar o significado dessas
palavras. Tendo ouvido do Abençoado, os bhikkhus o recordarão.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então
ouçam e prestem muita atenção àquilo que eu vou dizer.” – “Sim, venerável
senhor,” os bhikkhus responderam. </span>O Abençoado disse o seguinte:<span
lang=PT-BR style='mso-ansi-language:PT-BR'><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
bhikkhus, ao fazer uma investigação profunda, um bhikkhu investiga assim: ‘Os
muitos e variados tipos de sofrimento que surgem no mundo [encabeçados pelo]
envelhecimento e morte: qual é a fonte desse sofrimento, qual é a sua origem,
do que ele nasce e é produzido? O que existindo faz com que o envelhecimento e
morte surjam? O que não existindo faz com que o envelhecimento e morte não
surjam?’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ao
investigar profundamente ele compreende que: ‘Os muitos e variados tipos de
sofrimento que surgem no mundo [encabeçados pelo] envelhecimento e morte: esse
sofrimento tem o nascimento como fonte, nascimento como origem; ele nasce e é
produzido do nascimento. Quando há o nascimento, o envelhecimento e morte
surgem, quando não há o nascimento, o envelhecimento e morte não surgem.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele
compreende o envelhecimento e morte, a sua origem, a sua cessação e o caminho
que está em conformidade com a sua cessação. Ele pratica esse caminho e se
comporta de acordo. Esse é um bhikkhu que está praticando para a completa e
absoluta destruição do sofrimento, para a cessação do envelhecimento e morte. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
investigando mais, ele investiga assim: ‘Qual é a fonte desse nascimento, qual
é a sua origem, do que este nasce e é produzido? ... Qual é a fonte desse
ser/existir, qual é a sua origem, do que ele nasce e é produzido? ... desse
apego? ... desse desejo? ... dessa sensação? ... desse contato? ... dessas seis
bases dos sentidos? ... dessa mentalidade-materialidade (nome e forma)? ... dessa consciência?
... Qual é a fonte dessas formações volitivas, qual é a sua origem, do que elas nascem e
são produzidas? O que existindo faz com que as formações volitivas surjam? O que não
existindo faz com que as formações volitivas não surjam?'<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ao
investigar profundamente ele compreende que: ‘As formações volitivas têm a ignorância
como fonte, a ignorância como origem; elas nascem e são produzidas da
ignorância. Quando há ignorância, as formações volitivas surgem, quando não há
ignorância, as formações volitivas não surgem.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele
compreende as formações volitivas, a sua origem, a sua cessação e o caminho que está em
conformidade com a sua cessação. Ele pratica esse caminho e se comporta de
acordo. Esse é um bhikkhu que está praticando para a completa e absoluta
destruição do sofrimento, para a cessação das formações volitivas.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
se uma pessoa imersa na ignorância gera uma formação volitiva meritória, a
consciência vai para o meritório; se ela gera uma formação volitiva
demeritória, a consciência vai para o demeritório; se ela gera uma formação
volitiva imperturbável, a consciência vai para o imperturbável. Mas quando um
bhikkhu abandonou a ignorância e fez surgir o verdadeiro conhecimento, então,
com o desaparecimento da ignorância e o surgimento do verdadeiro conhecimento,
ele não gera uma formação volitiva meritória, ou uma formação volitiva
demeritória, ou uma formação volitiva imperturbável. Visto que ele não gera ou
cria formações volitivas, ele não se apega a nada no mundo. Sem se apegar a nada no mundo, ele não fica
agitado. Não estando agitado, ele realiza Nibbana. Ele compreende que: ‘O
nascimento foi destruído, a vida santa foi vivida, o que deveria ser feito foi
feito, não há mais vir a ser a nenhum estado.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se ele
sentir uma sensação prazerosa, ele compreende: ‘Ela é impermanente’; ele
compreende: ‘Não é para se apegar’; ele compreende: ‘Não é para se deleitar.’
Se ele sentir uma sensação dolorosa, ele compreende: ‘Ela é impermanente’; ele
compreende: ‘Não é para se apegar’; ele compreende: ‘Não é para se deleitar.’
Se ele sentir uma sensação nem prazerosa, nem dolorosa, ele compreende: ‘Ela é
impermanente’; ele compreende: ‘Não é para se apegar’; ele compreende: ‘Não é
para se deleitar.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se ele
sentir uma sensação prazerosa, ele a sente desapegado; se ele sentir uma
sensação dolorosa, ele a sente desapegado; Se ele sentir uma sensação nem
prazerosa, nem dolorosa, ele a sente desapegado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando ele
sente uma sensação que dá um fim ao corpo, ele compreende: ‘Eu sinto uma
sensação que dá um fim ao corpo.’ Quando ele sente uma sensação que dá um fim à
vida, ele compreende: ‘Eu sinto uma sensação que dá um fim à vida.’ Ele
compreende: ‘Com a dissolução do corpo, depois da morte, tudo que é sentido sem
deleite, irá esfriar aqui mesmo, apenas resíduos corporais restarão.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Suponham,
bhikkhus, que um homem removesse um pote de cerâmica do forno de oleiro e o
colocasse no chão liso: o seu calor iria se dissipar ali mesmo
e apenas cacos de cerâmica restariam. Da mesma forma, quando ele sente uma
sensação que dá um fim ao corpo ... que dá um fim à vida, ele compreende: ‘Eu
sinto uma sensação que dá um fim à vida.’ Ele compreende: ‘Com a dissolução do
corpo, depois da morte, tudo que é sentido sem deleite, irá esfriar aqui
mesmo, apenas resíduos corporais<span style="mso-spacerun: yes"> 
</span>restarão.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O que
vocês pensam, bhikkhus, um bhikkhu cujas impurezas foram destruídas poderá
gerar uma formação volitiva meritória, ou uma formação volitiva demeritória, ou
uma formação volitiva imperturbável?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
venerável senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando não
há, em absoluto, formações volitivas, com a cessação das formações volitivas, a consciência seria
discernida?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
venerável senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando não
há, em absoluto, a consciência, com a cessação da consciência, a
mentalidade-materialidade (nome e forma) seria discernida?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
venerável senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando não
há, em absoluto, a mentalidade-materialidade (nome e forma) ... seis bases dos sentidos ...
contato ... sensação ... apego ... ser/existir ... nascimento, com a cessação
do nascimento, o envelhecimento e morte seria discernido?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
venerável senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Muito bem,
bhikkhus! É exatamente assim e não de outra forma! Tenham fé em mim com
respeito a isso, bhikkhus, tenham certeza disso. Livrem-se da dúvida e
perplexidade com respeito a isso. Simplesmente, isso é o fim do sofrimento.”<b
style='mso-bidi-font-weight:normal'><o:p></o:p></b></span></p>


