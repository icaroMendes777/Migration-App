<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Certa
ocasião, o Ven. Pindola Bharadvaja estava em Kosambi no
monastério de Ghosita. Então, o Rei Udena
foi até ele e ao chegar eles se cumprimentaram. Quando a conversa cortês e
amigável havia terminado o rei sentou a um lado e disse para o <a name=PVW>Ven</a>.
Pindola Bharadvaja: <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Qual é a
razão, mestre Bharadvaja, qual é a causa, pela qual bhikkhus jovens, com o
cabelo negro, dotados com as bênçãos da juventude, no primeiro estágio da vida,
que apesar de não se terem entretido com os prazeres sensuais, adotam a vida
santa completamente perfeita e imaculada por toda a vida e mantêm-na<span
style="mso-spacerun: yes">  </span>por muito tempo?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“G<a name=kin>rande rei,</a>
isto foi dito pelo Abençoado que sabe e vê, um arahant, perfeitamente iluminado:
‘Venham agora, bhikkhus: com relação às mulheres que possuem idade suficiente
para ser a sua mãe, estabeleçam a idéia de que elas são a sua mãe. Com relação
às mulheres com idade suficiente para ser a sua irmã, estabeleçam a idéia de
que elas são a sua irmã. Com relação às mulheres que são jovens o suficiente
para ser a sua filha, estabeleçam a idéia de que elas são a sua filha.’ Essa é
uma razão, essa é uma causa, grande rei, pela qual bhikkhus jovens ... adotam a
vida santa completamente perfeita e imaculada por toda a vida e mantêm-na por
muito tempo.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“A mente é licenciosa,
mestre Bharadvaja. Algumas vezes pensamentos de cobiça surgem até mesmo em
relação a mulheres que possuem idade suficiente para ser nossa mãe ... irmã ... jovens
o suficiente para ser nossa filha. Existe alguma outra razão, outra causa, pela
qual bhikkhus jovens ... adotam a vida santa completamente perfeita e imaculada
por toda a vida e mantêm-na por muito tempo?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Grande rei, isto foi dito
pelo Abençoado que sabe e vê, um arahant, perfeitamente iluminado: ‘Venham agora,
bhikkhus: reflitam sobre este mesmo corpo, da sola dos pés para cima, do topo
da cabeça para baixo, envolto pela pele, cheio de todo tipo de coisas impuras:
‘Neste corpo existem cabelos, pêlos do corpo, unhas, dentes, pele, carne,
tendões, ossos, tutano, rins, coração, fígado, diafragma, baço, pulmões,
intestino grosso, intestino delgado, conteúdo do estômago, fezes, bílis,
fleuma, pus, sangue, suor, gordura, lágrimas, saliva, muco, líquido sinovial e
urina.’ Essa também é uma razão, essa também é uma causa, grande rei, pela qual
bhikkhus jovens ...<span style="mso-spacerun: yes">  </span>adotam a vida santa
completamente perfeita e imaculada por toda a vida e mantêm-na por muito
tempo.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Isso é fácil, mestre
Bharadvaja, para aqueles bhikkhus que são desenvolvidos em relação ao corpo,
desenvolvidos em relação à virtude, desenvolvidos em relação à mente,
desenvolvidos em relação à sabedoria. Mas é difícil para aqueles bhikkhus que
não são desenvolvidos em relação ao corpo, não são desenvolvidos em relação à
virtude, não são desenvolvidos em relação à mente, não são desenvolvidos em
relação à sabedoria. Algumas vezes, embora alguém pense, ‘Vou considerar isso
como pouco atrativo,’ aquilo passa a ser atrativo. Existe alguma outra razão,
outra causa, pela qual bhikkhus jovens … adotam a vida santa completamente perfeita
e imaculada por toda a vida e mantêm-na por muito tempo?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Grande rei, isto foi dito
pelo Abençoado que sabe e vê, um arahant, perfeitamente iluminado: ‘Venham agora,
bhikkhus: Guardem as portas dos meios dos sentidos. Ao ver uma forma com o
olho, não se agarrem </span>aos seus sinais ou detalhes<span lang=PT-BR
style='mso-ansi-language:PT-BR'>, pelo qual estados prejudiciais
de cobiça e tristeza poderiam
tomá-los se vocês permanecessem com a faculdade do olho </span>descuidada<span
lang=PT-BR style='mso-ansi-language:PT-BR'>. Pratiquem a contenção, protejam a
faculdade do olho, </span>se empenhem na contenção da faculdade do olho<span
lang=PT-BR style='mso-ansi-language:PT-BR'>.<span style="mso-spacerun: yes"> 
</span><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’Ao ouvir um som com o
ouvido... <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’Ao cheirar um aroma com o
nariz... <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’Ao saborear um sabor com
a língua... <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’Ao sentir um tangível com
o corpo…<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’Ao conscientizar um objeto
mental com a mente, não se agarrem </span>aos seus sinais ou detalhes<span
lang=PT-BR style='mso-ansi-language:PT-BR'>, pelo qual estados prejudiciais </span>de
cobiça e tristeza<span lang=PT-BR style='mso-ansi-language:PT-BR'> poderiam
tomá-los se vocês permanecessem com a faculdade da mente </span>descuidada<span
lang=PT-BR style='mso-ansi-language:PT-BR'>. Pratiquem a contenção, protejam a
faculdade da mente, </span>se empenhem na contenção da faculdade da mente<span
lang=PT-BR style='mso-ansi-language:PT-BR'>.’ <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Essa também é uma razão,
essa também é uma causa, grande rei, pela qual bhikkhus jovens ... adotam a
vida santa completamente perfeita e imaculada por toda a vida e mantêm-na por
muito tempo.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“É maravilhoso, mestre
Bharadvaja! É impressionante, mestre Bharadvaja! Quão bem isso foi dito pelo
Abençoado que sabe e vê, um arahant, perfeitamente iluminado! Então, essa é a
razão, essa é a causa pela qual bhikkhus jovens com o cabelo negro, dotados com
as bênçãos da juventude, no primeiro estágio da vida, que apesar de não se
terem entretido com os prazeres sensuais, adotam a vida santa completamente perfeita
e imaculada por toda a vida e mantêm-na por muito tempo. No meu caso também,
quando entro no meu harém sem guardar o corpo, a linguagem, a mente, sem
estabelecer a atenção plena e sem a contenção dos sentidos, sou assaltado pelos
pensamentos de cobiça. Mas sempre que entro no meu harém guardando o corpo, a
linguagem, a mente, com a atenção plena estabelecida e com os sentidos
contidos, não sou assaltado pela cobiça..<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Magnífico, Mestre
Bharadvaja! Magnífico, Mestre Bharadvaja! Mestre Bharadvaja esclareceu o Dhamma
de várias formas, como se tivesse colocado em pé o que estava de cabeça para
baixo, revelasse o que estava escondido, mostrasse o caminho para alguém que
estivesse perdido ou segurasse uma lâmpada no escuro para aqueles que possuem
visão pudessem ver as formas. Eu busco refúgio no Abençoado, no Dhamma e na
Sangha dos bhikkhus. Que o Mestre Bharadvaja se lembre de mim como o discípulo
leigo que buscou refúgio para o resto da vida.” <o:p></o:p></span></p>


