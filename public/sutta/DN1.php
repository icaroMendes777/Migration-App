estado. Eles são permanentes, intermináveis, eternos, não sujeitos à mudança e
que irão durar tanto tempo quanto a eternidade. Mas nós, que fomos corrompidos
pelo prazer, gastamos um tempo excessivo nos regozijando, brincando e nos
divertindo. Portanto, nós, com a dissipação da atenção plena, deixamos aquele
estado, nós somos impermanentes, terminantes, transitórios, sujeitos à mudança,
com a vida curta e nós viemos para este mundo.’ Esse é o segundo modo.<o:p></o:p></span></p>



<p class=Normal><a name="T2_10"></a><span lang=PT-BR style='mso-ansi-language:
