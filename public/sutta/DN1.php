<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.1. “Há,
bhikkhus, alguns contemplativos e Brâmanes que afirmam em parte a eternidade e
em parte a não-eternidade, que afirmam a eternidade parcial e a não-eternidade
parcial do eu e do mundo de quatro modos distintos. Com base em que?<o:p></o:p></span></p>



<p class=Normal><a name="T2_2"></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2.2. “Haverá um tempo, bhikkhus, quando, cedo ou tarde, após um longo
período, este mundo irá se contrair. Numa época de contração, os seres nascem
principalmente no mundo de Abhassara.[<a href="#N8">8</a>] <a name=R8></a>E
lá eles permanecem feitos de mentalidade,[<a href="#N9">9</a>] <a name=R9></a>alimentados
pelo êxtase, [<a href="#N10">10</a>] <a name=R10></a>luminosos, movendo-se através
do espaço, gloriosos – e assim eles permanecem por um longo tempo. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.3.
[Entendimento Incorreto 5] “Mas cedo ou tarde, depois de um longo período, o
mundo começa novamente a se expandir. Nesse mundo em expansão aparece um
palácio de Brahma vazio. [<a href="#N11">11</a>] <a name=R11></a>Então um ser,
com a exaustão do seu tempo de vida ou dos seus méritos, [<a href="#N12">12</a>]
<a name=R12></a>deixa o mundo de Abhassara<span style='mso-spacerun:yes'> 
</span>e renasce no palácio de Brahma vazio. E lá ele permanece, feito de
mentalidade, alimentado pelo êxtase, luminoso, movendo-se através do espaço,
glorioso – e assim ele permanece por um longo tempo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.4.
“Então, nesse ser que ficou só por tanto tempo, surge a inquietação, o
descontentamento e a preocupação, ele pensa: ‘Oh, se pelo menos alguns outros
seres aqui viessem!’ E outros seres, com a exaustão do seu tempo de vida ou dos
seus méritos, deixam o mundo de Abhassara<span style='mso-spacerun:yes'> 
</span>e renascem no palácio de Brahma como companheiros daquele ser. E lá eles
permanecem, feitos de mentalidade ... e assim eles permanecem por um longo
tempo.<o:p></o:p></span></p>



<p class=Normal><a name="T2_5"></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2.5. “Depois, bhikkhus, aquele ser que primeiro ali surgiu pensa: ‘Eu
