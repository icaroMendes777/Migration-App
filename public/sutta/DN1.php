name=R13></a>Eles gastam um tempo excessivo se regozijando, brincando e se
divertindo, de modo que a sua atenção plena é dissipada e com a dissipação da
atenção plena aqueles seres deixam aquele estado. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.8. “E
pode acontecer que algum daqueles seres que deixaram aquele mundo renasça neste
