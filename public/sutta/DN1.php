aquele Brahma, nós somos impermanentes, terminantes, transitórios, sujeitos à
mudança, com a vida curta e nós viemos para este mundo.’ Esse é o primeiro modo
através do qual alguns contemplativos e Brâmanes afirmam em parte a Eternidade
e em parte a Não-eternidade.<o:p></o:p></span></p>



<p class=Normal><a name="T2_7"></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2.7. [Entendimento Incorreto 6] “E qual é o segundo modo? Há, bhikkhus,
