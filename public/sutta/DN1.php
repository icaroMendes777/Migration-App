Dessa forma a mente deles é corrompida. Por conta da mente corrompida o corpo e
a mente deles ficam exaustos. E eles deixam aquele estado. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.11“E pode
