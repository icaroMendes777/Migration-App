Todo Poderoso, Senhor, Deus e Criador, Soberano, Providência Divina, Pai de
todos aqueles que são e serão. Esses seres foram criados por mim. Como assim?
Porque eu primeiro pensei: “Oh, se pelo menos alguns outros seres aqui
viessem!” Esse foi o meu desejo e assim esses seres foram aqui criados.’ E
aqueles seres que ali renasceram também pensam: ‘Ele, amigos, é Brahma,<span
