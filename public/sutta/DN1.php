<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.1. Assim
ouvi. Em certa ocasião o Abençoado estava caminhando pela estrada principal
entre Rajagaha e Nalanda [<a href="#N1">1</a>] <a name=R1></a>acompanhado por
uma comitiva de uns quinhentos bhikkhus. E o errante Suppiya também estava
caminhando pela mesma estrada com o seu jovem pupilo Brahmadatta. E Suppiya [<a
href="#N2">2</a>] <a name=R2></a>estava censurando de várias formas o Buda, o
Dhamma e a Sangha, enquanto o seu pupilo Brahmadatta estava elogiando de várias
formas o Buda, o Dhamma e a Sangha. E assim aqueles dois, mestre e pupilo, com
opiniões contrárias, estavam seguindo de perto o Abençoado e a sua comitiva de
bhikkhus. <o:p></o:p></span></p>



<p class=Normal><a name="T1_2"></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>1.2. Então o Abençoado parou para pernoitar com os bhikkhus no parque
real de<span style='mso-spacerun:yes'>  </span>Ambalatthika. E Suppiya com o
seu pupilo Brahmadatta também parou no mesmo parque para pernoitar. E Suppiya
seguiu censurando de várias formas o Buda, o Dhamma e a Sangha, enquanto o seu
pupilo Brahmadatta os defendia. E assim discutindo eles estavam perto do
Abençoado e da sua comitiva de bhikkhus.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.3. Agora,
ao amanhecer, um grande número de bhikkhus, depois de se levantarem,<span
style='mso-spacerun:yes'>  </span>reuniram-se no Pavilhão Circular e estavam
conversando sobre o seguinte: “É maravilhoso, amigos, é admirável como o
Abençoado, um arahant, perfeitamente iluminado, que sabe e vê, claramente distingue as
diferentes inclinações dos seres! Pois aqui está o errante Suppiya censurando
de várias formas o Buda, o Dhamma e a Sangha, enquanto o seu pupilo Brahmadatta
os defende. E, ainda discutindo, eles seguem de perto o Abençoado e a comitiva
de bhikkhus.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.4. Então,
o Abençoado percebendo sobre o que os bhikkhus estavam conversando, foi até o
Pavilhão Circular e sentou num assento que havia sido preparado. Então ele
disse: “Bhikkhus, qual é o assunto que faz com que vocês estejam sentados
juntos aqui agora? E qual é a discussão que foi interrompida?” E eles lhe
disseram.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.5.
“Bhikkhus, se alguém falar difamando o Buda, o Dhamma ou a Sangha, vocês não
deveriam ficar com raiva, ressentimento ou perturbação por conta disso. Se
vocês ficarem com raiva ou descontentes, isso será apenas um obstáculo para
vocês. Pois se outros difamam o Buda, o Dhamma ou a Sangha e vocês ficarem com
raiva ou descontentes, será possível que vocês saibam se aquilo que eles dizem
é correto ou não?” “Não, venerável senhor.” “Se alguém difamar o Buda, o Dhamma
ou a Sangha, então vocês devem explicar aquilo que é incorreto como incorreto,
dizendo: ‘Isso é incorreto, isso é falso, esse não é o nosso modo, esse tipo de
coisa não é encontrada no nosso meio’.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.6. “Mas,
bhikkhus, se alguém falar elogiando o Buda, o Dhamma, ou a Sangha, vocês não deveriam
ficar satisfeitos, contentes ou exultantes por conta disso. Se vocês ficarem
satisfeitos, contentes ou exultantes com o elogio, isso será apenas um
obstáculo para vocês. Se alguém elogiar o Buda, o Dhamma, ou a Sangha, vocês
devem reconhecer a verdade daquilo que é verdadeiro, dizendo: ‘Isso é correto,
isso é verdadeiro, esse é o nosso modo, esse tipo de coisa é encontrada no
nosso meio’.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.7.
“Bhikkhus, é com relação a questões elementares e subordinadas no que diz
respeito à prática da virtude [<a href="#N3">3</a>] <a name=R3></a>que a pessoa
comum [<a href="#N4">4</a>] <a name=R4></a>elogiaria o Tathagata. E quais são
essas questões elementares e subordinadas pelas quais a pessoa comum o
elogiaria?”<o:p></o:p></span></p>



<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>
(A Pequena Seção sobre a Virtude)</b></o:p></span></b></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.8.
“’Abandonando tirar a vida de outros seres, o contemplativo Gotama se abstém
de tirar a vida de outros seres; ele permanece com a sua vara e arma postas de
lado, bondoso e gentil, compassivo com todos os seres vivos.” Assim a pessoa
comum elogiaria o Tathagata.[<a href="#N5">5</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R5></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’Abandonando
tomar o que não é dado, o contemplativo Gotama se abstém de tomar o que não é
dado. Ele somente toma aquilo que é dado, aceita somente aquilo que é
dado.<span style='mso-spacerun:yes'>  </span><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’Abandonando
o não celibato, o contemplativo Gotama vive uma vida celibatária, vive
separado, abstendo-se da prática vulgar do ato sexual. <o:p></o:p></span></p>

<p class=Normal><a name="T1_9"></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.9.
“’Abandonando a linguagem mentirosa, o contemplativo Gotama se abstém da
linguagem mentirosa. Ele fala a verdade, mantém a verdade, é firme e confiável,
não é um enganador do mundo. Abandonando a linguagem maliciosa, ele se abstém
da linguagem maliciosa. O que ele ouviu aqui ele não conta ali para separar
aquelas pessoas destas. O que ele ouviu lá ele não conta aqui para separar
estas pessoas daquelas. Assim ele reconcilia aquelas pessoas que estão
divididas, promove a amizade, ele ama a concórdia, se delicia com a concórdia,
desfruta da concórdia, diz coisas que criam a concórdia. Abandonando a
linguagem grosseira, ele se abstém da linguagem grosseira. Ele diz palavras que
são gentis, que agradam aos ouvidos, carinhosas, que penetram o coração, que
são corteses, desejadas por muitos e que agradam a muitos. Abandonando a
linguagem frívola, ele se abstém da linguagem frívola. Ele fala na hora certa,
diz o que é fato, aquilo que é bom, fala de acordo com o Dhamma e a Disciplina;
nas horas adequadas ele diz palavras que são úteis, racionais, moderadas e que
trazem benefício. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.10. “O contemplativo
Gotama se abstém de danificar sementes e plantas. Ele come somente uma vez ao
dia, privando-se da refeição noturna e de alimentos nas horas incorretas. Ele
se abstém de dançar, cantar, música e de espetáculos teatrais. Ele se abstém de
usar ornamentos e de embelezar-se com perfumes e cosméticos. Ele se abstém de
leitos e assentos luxuosos. Ele se abstém de aceitar ouro e dinheiro. Ele se
abstém de aceitar grãos que não estejam cozidos ... carne crua ... mulheres e
garotas ... escravos e escravas ... cabras e ovelhas ... aves e porcos ...
elefantes, gado, cavalos e éguas ... terras e propriedades. Ele se abstém de
fazer pequenas tarefas e levar mensagens ... de comprar e vender ... de lidar
com balanças falsas, metais falsos, falsas medidas ... suborno, burla e fraude.
Ele se abstém de mutilar, executar, aprisionar, roubar, pilhar e violentar.”<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(A Seção
Intermediária sobre a Virtude)</b><o:p></o:p></span></b></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.11.
“’Enquanto alguns brâmanes e contemplativos, vivendo de alimentos dados em boa
fé, estão habituados a danificar sementes e plantas como estas - plantas que se
propagam pelas raízes, caules, juntas, germinações e sementes - o contemplativo
Gotama se abstém de danificar sementes e plantas como essas. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.12.
“’Enquanto alguns brâmanes e contemplativos, vivendo de alimentos dados em boa
fé, estão habituados a consumir mercadorias armazenadas tais como estas - comida
armazenada, bebidas armazenadas, roupas armazenadas, veículos armazenados,
roupas de cama armazenadas, perfumes armazenados, carne armazenada - o
contemplativo Gotama se abstém de consumir mercadorias armazenadas como essas.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.13.
“’Enquanto alguns brâmanes e contemplativos, vivendo de alimentos dados em boa
fé, estão habituados a assistir espetáculos como estes – danças, canto,
instrumentos musicais, recitações de baladas, bater palmas, címbalos e
tambores, cenas pintadas, truques acrobáticos e de súplicas, luta de elefantes,
luta de cavalos, luta de búfalos, touradas, luta de bodes, luta de carneiros,
briga de galos, luta de codornas; luta com paus, boxe, luta livre, jogos de
guerra, listas de chamadas, planos de batalha, e<span
style='mso-spacerun:yes'>  </span>paradas militares – o contemplativo Gotama se
abstém de assistir espetáculos como esses.<o:p></o:p></span></p>



<p class=Normal><a name="T1_14"></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>1.14. “’Enquanto alguns brâmanes e contemplativos, vivendo de alimentos
dados em boa fé, estão habituados a jogos insensatos e ociosos como estes –
xadrez de oito filas, xadrez de dez filas, xadrez no ar, amarelinha, jogo de
varetas, dados, jogos com paus, desenhos com a mão, jogos com bola, soprar
flautas de brinquedo, brincar com arados de brinquedo, piruetas, brincar com
moinhos de vento de brinquedo, medidas de brinquedo, carruagens de brinquedo,
arcos de brinquedo, adivinhar letras desenhadas no ar, adivinhar pensamentos,
imitando deformidades – o contemplativo Gotama se abstém de jogos insensatos e
ociosos como esses.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.15.
“’Enquanto alguns brâmanes e contemplativos, vivendo de alimentos dados em boa
fé, estão habituados a uma mobília alta e luxuosa como essas – sofás de grande
tamanho, enfeitados com animais esculpidos; colchas felpudas, com retalhos
multicoloridos, de lã branca, de lã bordadas com flores ou figuras de animais,
acolchoadas, com franjas, de seda bordadas com pedras preciosas; carpetes
grandes de lã; tapetes com elefantes, cavalos e carruagens, tapetes de pele de
antílope, tapetes de pele de gamo; camas com baldaquino, com almofadas
vermelhas para a cabeça e os pés – o contemplativo Gotama se abstém de usar
mobílias altas e luxuosas como essas.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.16.
“’Enquanto alguns brâmanes e contemplativos, vivendo de alimentos dados em boa
fé, estão habituados a perfumes, cosméticos, e meios de embelezamento como
estes – pós para massagear o corpo, massagem com óleos, banho em água
perfumada, massagem nos membros, usar espelhos, pomadas, ornamentos, perfumes,
cremes, pós de arroz, máscara, braceletes, tiaras, bengalas decoradas, garrafas
de água ornamentadas, espadas, guarda sóis enfeitados, sandálias decoradas,
turbantes, pedras preciosas, espanador feito de pelos de yak, túnicas longas
com franjas – o contemplativo Gotama se abstém de usar perfumes, cosméticos e
meios de embelezamento como esses.<o:p></o:p></span></p>



<p class=Normal><a name="T1_17"></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>1.17. “’Enquanto alguns brâmanes e contemplativos, vivendo de alimentos
dados em boa fé, estão habituados a falar de tópicos inferiores como estes –
falar sobre reis, ladrões, ministros de estado, exércitos, alarmes e batalhas;
comida e bebida, roupas, mobília, ornamentos e perfumes, parentes; veículos;
vilarejos, vilas, cidades, o campo; mulheres e heróis; as fofocas das ruas e do
poço; contos dos mortos; contos da diversidade (discussões filosóficas do
passado e futuro), a criação do mundo e do mar e conversa acerca de se as
coisas existem ou não – o contemplativo Gotama se abstém de falar de tópicos
inferiores<span style='mso-spacerun:yes'>  </span>como esses.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.18.
“’Enquanto alguns brâmanes e contemplativos, vivendo de alimentos dados em boa
fé, estão habituados a debates tais como estes – ‘<i style='mso-bidi-font-style:
normal'>Você </i>entende esta doutrina e disciplina? <i style='mso-bidi-font-style:
normal'>Eu </i>sou aquele que entende esta doutrina e disciplina. Como pode
você entender esta doutrina e disciplina? A sua prática é incorreta. Eu pratico
corretamente. Eu sou consistente. Você não é. O que deve ser dito primeiro você
fala por último. O que deve ser dito por último você fala primeiro. O que lhe
tardou tanto tempo para pensar foi refutado. A sua doutrina foi derrubada. Você
está derrotado. Vá e tente salvar a sua doutrina; solte a si mesmo se você
puder!’ – o contemplativo Gotama se abstém de debates tais como estes.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.19.
“’Enquanto alguns brâmanes e contemplativos, vivendo de alimentos dados em boa
fé, estão habituados a levar mensagens e fazer pequenas tarefas para pessoas tais
como estas – reis, ministros de estado, nobres guerreiros, brâmanes,
contemplativos, chefes de família, ou jovens (que dizem), ‘Venha aqui, vá ali,
leve isto lá, vá buscar e traga isto aqui’ – o contemplativo Gotama se abstém
de levar mensagens e fazer pequenas tarefas para pessoas como essas.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.20.
“’Enquanto alguns brâmanes e contemplativos, vivendo de alimentos dados em boa
fé, se ocupam com tramar, persuadir,<span style='mso-spacerun:yes'> 
</span>aconselhar, depreciar e perseguir o ganho com o ganho, o contemplativo
Gotama se abstém de tramar e persuadir (maneiras inadequadas de obter apoio
material de doadores).<o:p></o:p></span></p>



<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>(A
Grande Seção sobre a Virtude)</span></b><span lang=PT-BR style='mso-ansi-language:
PT-BR'><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.21. “’Enquanto alguns
brâmanes e contemplativos, vivendo de alimentos dados em boa fé, mantêm a si
mesmos através de um modo de vida incorreto, por meio de artes aviltantes assim
como: leitura de marcas nos membros [ex: quiromancia]; leitura de presságios e
sinais; interpretação de eventos celestiais [estrelas cadentes, cometas];
interpretação de sonhos; leitura de marcas no corpo [ex: frenologia]; leitura
de marcas em tecidos roídos por ratos; oblação oferecida com o fogo, oblação de
uma concha, oblação de palhas, pó de arroz, gorduras, e óleo; oferecer oblações
com a boca; oferecer sacrifícios de sangue; fazer previsões baseadas nas pontas
dos dedos; geomancia; deitar demônios em um cemitério; colocar feitiços em
espíritos; recitar feitiços protetores em casas; encantar serpentes, xamanismo
com venenos, xamanismo com escorpiões, xamanismo com ratos, xamanismo com
pássaros, xamanismo com corvos; ler a sorte com base em visões; dar encantos
protetores; interpretar o chamado de pássaros e animais, o contemplativo Gotama
se abstém do modo de vida incorreto, através de artes aviltantes como essas. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.22. “’Enquanto
alguns brâmanes e contemplativos, vivendo de alimentos dados em boa fé, mantêm
a si mesmos através de um modo de vida incorreto, através de artes aviltantes
assim como: determinar o que traz e não traz a sorte com pedras preciosas,
roupas, bengalas, espadas, lanças, flechas, arcos, e outras armas; mulheres,
meninos, meninas, escravos, escravas; elefantes, cavalos, búfalos, touros,
vacas, bodes, carneiros, aves, codornas, lagartos, roedores com orelhas
grandes, tartarugas, e outros animais – o contemplativo Gotama se abstém do
modo de vida incorreto, através de artes aviltantes como essas.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.23. “’Enquanto alguns
brâmanes e contemplativos, vivendo de alimentos dados em boa fé, mantêm a si
mesmos através de um modo de vida incorreto, através de artes aviltantes como
prever que: os regentes marcharão para a frente; os regentes marcharão para a
frente e retornarão; nossos regentes atacarão e os regentes deles recuarão; os
regentes deles atacarão e os nossos regentes recuarão; haverá triunfo para os nossos
regentes e derrota para os regentes deles; haverá triunfo para os regentes
deles e derrota para os nossos regentes; dessa forma haverá triunfo, dessa
forma haverá derrota - o contemplativo Gotama se abstém do modo de vida
incorreto, através de artes aviltantes como essas.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.24. “’Enquanto alguns
brâmanes e contemplativos, vivendo de alimentos dados em boa fé, mantêm a si
mesmos através de um modo de vida incorreto, através de artes aviltantes como
prever que: haverá um eclipse lunar; haverá um eclipse solar; haverá a
ocultação de um astro; o sol e a lua continuarão no seu curso normal; o sol e a
lua sairão do caminho; os astros seguirão no seu curso normal; os astros sairão
do caminho; haverá uma chuva de meteoros; haverá um escurecimento do céu; haverá
um terremoto; haverá trovões vindo de um céu claro; haverá um nascente, um poente,
um escurecimento, um brilho mais intenso do sol, lua, e astros; assim será o
resultado do eclipse lunar...o nascente, o poente, o escurecimento, o brilho mais intenso
do sol, lua e astros - o contemplativo Gotama se abstém do modo de vida
incorreto, através de artes aviltantes como essas<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.25. “’Enquanto alguns
brâmanes e contemplativos, vivendo de alimentos dados em boa fé, mantêm a si
mesmos através de um modo de vida incorreto, por meio de artes aviltantes como
prever que: haverá chuva em abundância; haverá uma seca; haverá abundância;
haverá fome; haverá paz e segurança; haverá insegurança; haverá doença; não
haverá doença; ou eles ganham a vida fazendo contas, contabilidade, cálculos, compondo
poesias, ou ensinando artes e doutrinas hedonísticas<span
style='mso-spacerun:yes'>  </span>- o contemplativo Gotama se abstém do modo de
vida incorreto, através de artes aviltantes como essas.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.26. “’Enquanto alguns
brâmanes e contemplativos, vivendo de alimentos dados em boa fé, mantêm a si
mesmos através de um modo de vida incorreto, por meio de artes aviltantes como:
calcular as datas mais auspiciosas para casamentos, noivados, divórcios; para
cobrar dívidas ou fazer investimentos e empréstimos; para ser atraente ou não
atraente; curar mulheres que abortaram; recitar feitiços para amarrar a língua
de um homem, para paralisar a sua mandíbula, para fazer com que ele perca o
controle das mãos, ou torná-lo surdo; obter respostas de oráculo a questões
dirigidas a um espelho, a uma jovem garota, ou a um médium espiritual; cultuar
o sol, cultuar o Grande Brahma, fazer sair chamas pela boca, invocar à deusa da
sorte - o contemplativo Gotama se abstém do modo de vida incorreto, através de
artes aviltantes como essas.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.27. “’Enquanto alguns brâmanes
e contemplativos, vivendo de alimentos dados em boa fé, mantêm a si mesmos
através de um modo de vida incorreto, através de artes aviltantes como:
prometer presentes para divindades em troca de favores; cumprir essas
promessas; demonologia; ensinar feitiços que protegem as casas; induzir
virilidade e impotência; consagrar terrenos para construção; dar colutórios
cerimoniais e banhos cerimoniais; oferecer fogueiras de sacrifício; preparar
eméticos, purgativos, expectorantes, diuréticos, curas para dor de cabeça;
preparar óleo para os ouvidos, gotas para os olhos, óleo para tratamento
através do nariz, colírio e antídotos; curar cataratas, praticar cirurgia,
praticar como pediatra, prescrever medicamentos e tratamentos para curar
efeitos colaterais - o contemplativo Gotama se abstém do modo de vida
incorreto, através de artes aviltantes<span style='mso-spacerun:yes'> 
</span>como essas. Bhikkhus, é com relação a essas questões elementares e
subordinadas no que diz respeito à prática da virtude que a pessoa comum
elogiaria o Tathagata.”<o:p></o:p></span></p>

