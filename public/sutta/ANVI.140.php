<p class=Normal> (140) "Bhikkhus, para o conhecimento direto da cobiça, seis coisas devem ser desenvolvidas. Quais seis? A visão insuperável, a audição insuperável, o ganho insuperável, o treinamento insuperável, o serviço insuperável, a recordação insuperável. Para o conhecimento direto da cobiça, essas seis coisas devem ser desenvolvidas." </p>

<p class=Normal> (141) "Bhikkhus, para o conhecimento direto da cobiça, seis coisas devem ser desenvolvidas. Quais seis? Recordação do Buda, recordação  do Dhamma, recordação da Sangha, recordação do comportamento virtuoso, recordação  da generosidade, recordação  dos devas. Para o conhecimento direto da cobiça, essas seis coisas devem ser desenvolvidas." </p>

<p class=Normal> (142) "Bhikkhus, para o conhecimento direto da cobiça, seis coisas devem ser desenvolvidas. Quais seis? A percepção da impermanência, a percepção do sofrimento naquilo que é impermanente, a percepção de não-eu naquilo que é sofrimento, a percepção de abandono, a percepção de desapego, a percepção da cessação. Para o conhecimento direto da cobiça, essas seis coisas devem ser desenvolvidas." </p>

<p class=Normal> (143-169) "Bhikkhus, para a completa compreensão da cobiça ... para a completa destruição ... para o abandono ... para a destruição ... para o desaparecimento ... para o desapego ... para a cessação .... para abrir mão ... para a renúncia da cobiça, essas seis coisas devem ser desenvolvidas." </p>

<p class=Normal> (170-649) "Bhikkhus, para o conhecimento direto ... para a completa compreensão .... para a completa destruição ... para o abandono ... para a destruição ... para o desaparecimento ... para o desapego ... para a cessação .... para abrir mão ... para a renúncia da raiva ... da delusão ... do ódio ... da hostilidade ... da difamação ... da insolência ... da inveja ... da avareza ... da enganação ... da astúcia ... da teimosia .... da veemência ... da presunção ... da arrogância ... da embriaguez .... da negligência, essas seis coisas devem ser desenvolvidas." </p>


