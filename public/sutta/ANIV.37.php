<p class=Normal> 
“Favorecido com quatro qualidades, um bhikkhu é incapaz de decair e
está próximo da libertação. Quais quatro?<o:p></o:p></span></p>




<p class=Normal>
“Há o caso em que o bhikkhu é perfeito em virtude, guarda as portas dos
meios dos sentidos, conhece a moderação no comer e é dedicado à vigilância.<o:p></o:p></span></p>




<p class=Normal>
“ E como um bhikkhu é perfeito em virtude? É o caso em que o bhikkhu é
virtuoso. Ele permanece contido de acordo com as regras do Patimokkha, perfeito
na conduta e na sua esfera de atividades. Temendo a menor falha, ele treina
adotando os preceitos de virtude.’ Assim é como um Bhikkhu é perfeito em
virtude.<o:p></o:p></span></p>




<p class=Normal>
“ E como um bhikkhu guarda as portas dos meios dos sentidos? É o caso
em que o bhikkhu ao ver uma
forma com o olho, ele não se agarra aos seus sinais ou detalhes. Visto que, se
permanecer com a faculdade do olho descuidada, ele será tomado pelos estados
ruins e prejudiciais de cobiça e tristeza. Ele pratica a contenção, ele protege
a faculdade do olho, ele se empenha na contenção da faculdade do olho.<o:p></o:p></span></p>




<p class=Normal>
“ Ao ouvir um som com o ouvido.....<o:p></o:p></span></p>

<p class=Normal>
“ Ao cheirar um aroma com o nariz....<o:p></o:p></span></p>

<p class=Normal>
“ Ao saborear um sabor com a língua....<o:p></o:p></span></p>


<p class=Normal> “ Ao tocar um tangível com o corpo....<o:p></o:p></span></p>




<p class=Normal>
“Ao conscientizar um objeto
mental<span style="mso-spacerun: yes">  </span>com a<span style="mso-spacerun:
yes">  </span>mente, ele não se agarra aos seus sinais ou detalhes. Visto que,
se permanecer com a faculdade da mente descuidada, ele será tomado pelos
estados ruins e prejudiciais de cobiça e tristeza. Ele pratica a contenção, ele
protege a faculdade da mente, ele se empenha na contenção da faculdade da
mente.</span>
Essa é a forma como um bhikkhu guarda as portas dos meios dos sentidos.<o:p></o:p></span></p>




<p class=Normal>
“ E como um bhikkhu conhece a moderação no comer? É o caso em que o
bhikkhu, refletindo de maneira sábia: o alimento não deve ser tomado como forma
de diversão ou para embriaguez, tampouco com o objetivo de embelezamento e para
ser mais atraente porém, somente com o propósito de manter a resistência e
continuidade desse corpo, como forma de dar um fim ao desconforto e para
auxiliar a vida santa. Considerando: “Dessa forma darei um fim às antigas
sensações (de fome) sem despertar novas sensações (de comida em excesso) e
serei saudável e sem culpa e viverei em comodidade.” <o:p></o:p></span></p>




<p class=Normal>
“ E como um bhikkhu é dedicado à vigilância? É o caso em que o bhikkhu,
durante o dia, enquanto estiver caminhando para lá e para cá e sentado,
purifica a sua mente dos estados obstrutivos. Na primeira vigília da noite,
enquanto estiver caminhando para lá e para cá e sentado, ele purifica a sua
mente dos estados obstrutivos. Na segunda vigília da noite ele se deita para
dormir, no seu lado direito, na postura do leão com um pé sobre o outro, atento
e plenamente consciente, após anotar na sua mente o horário para levantar. Após
levantar-se, na terceira vigília da noite, enquanto estiver caminhando para cá
e para lá e sentado, ele purifica a sua mente dos estados obstrutivos.<o:p></o:p></span></p>




<p class=Normal>
“ Favorecido com essas quatro qualidades, um bhikkhu é incapaz de
decair e está próximo da libertação.<o:p></o:p></span></p>





<p class=Normal style='margin-left:.75in'>O bhikkhu com a
