<p class=Normal> “Bhikkhus, eu lhes ensinarei o Dhamma para a exaustão de todo o apego. Ouçam e prestem muita atenção àquilo que eu vou dizer.” – “Sim, venerável senhor,” os bhikkhus responderam. O Abençoado disse o seguinte:</p>

<p class=Normal> "Qual, bhikkhus, é o Dhamma para a exaustão de todo o apego? O que vocês pensam, bhikkhus, o olho é permanente ou impermanente?” – “Impermanente, venerável senhor.” – “E aquilo que é impermanente é sofrimento ou felicidade?” – “Sofrimento, venerável senhor.” – “E é adequado considerar o que é impermanente, sofrimento, sujeito a mudanças como: ‘Isso é meu. Isso sou eu. Isso é o meu eu’?” – “Não, venerável senhor.”

<p class=Normal> “As formas são permanentes ou impermanentes? ... A consciência no olho ... O contato no olho ... Qualquer sensação que surja tendo o contato no olho como condição – quer seja prazerosa, dolorosa ou nem prazerosa, nem dolorosa – é permanente ou impermanente? ...

<p class=Normal> “O ouvido é permanente ou impermanente? ... A mente ... Qualquer sensação que surja tendo o contato na mente como condição – quer seja prazerosa, dolorosa ou nem prazerosa, nem dolorosa – é permanente ou impermanente?” – “Impermanente, venerável senhor.” – “E aquilo que é impermanente é sofrimento ou felicidade?” – “Sofrimento, venerável senhor.” – “E é adequado considerar o que é impermanente, sofrimento, sujeito a mudanças como: ‘Isso é meu. Isso sou eu. Isso é o meu eu’?” – “Não, venerável senhor.”</p>

<p class=Normal> “Vendo isso, o nobre discípulo bem instruído experimenta o desencantamento  em relação em relação ao olho, em relação às formas, em relação à consciência no olho, em relação ao contato no olho, em relação a qualquer sensação que surja tendo o contato no olho como condição – quer seja prazerosa, dolorosa ou nem prazerosa, nem dolorosa ... em relação à mente, em relação aos objetos mentais, em relação à consciência na mente, em relação ao contato na mente, em relação a qualquer sensação que surja tendo o contato na mente como condição – quer seja prazerosa, dolorosa ou nem prazerosa, nem dolorosa. Experimentando o desencantamento ele se torna desapegado. Através do desapego a sua mente está libertada. Com a libertação ele compreende: 'O apego foi exaurido por mim.'</p>

<p class=Normal> "Esse bhikkhus, é o Dhamma para a exaustão de todo o apego."</p>

