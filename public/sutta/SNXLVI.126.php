<p class=Normal> Em Savatthi. “Bhikkhus, há esses cinco elementos do prazer sensual. Quais cinco? Formas conscientizadas através do olho que são desejáveis, agradáveis e fáceis de serem gostadas, conectadas com o desejo sensual e que provocam a cobiça. Sons conscientizados através do ouvido … Aromas conscientizados através do nariz … Sabores conscientizados através da língua …Tangíveis conscientizados através do corpo  que são desejáveis, agradáveis e fáceis de serem gostados, conectados com o desejo sensual e que provocam a cobiça. Essas são os cinco elementos do prazer sensual. Os sete fatores da iluminação devem ser desenvolvidos para o conhecimento direto desses cinco elementos do prazer sensual, para a sua completa compreensão, para a sua total destruição e para o seu abandono.” [<a href="#N1">1</a>]<o:p></o:p></span></p>


