<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
não pensem pensamentos ruins e prejudiciais; isto é, pensamentos de desejo
sensual, pensamentos de má vontade e pensamentos de crueldade. Por qual razão?
Esses pensamentos, bhikkhus, não trazem benefícios, não pertencem aos
fundamentos da vida santa e não conduzem ao desencantamento, ao desapego, à
cessação, à paz, ao conhecimento direto, à iluminação, a Nibbana. <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando
pensarem, bhikkhus, vocês deveriam pensar: ‘Isto é sofrimento’; vocês deveriam
pensar: ‘Esta é a origem do sofrimento’; vocês deveriam pensar: ‘Esta é a
cessação do sofrimento’; vocês deveriam pensar: ‘Este é o caminho que conduz à
cessação do sofrimento.’ Por qual razão? Esses pensamentos, bhikkhus, trazem
benefícios, pertencem aos fundamentos da vida santa e conduzem ao
desencantamento, ao desapego, à cessação, à paz, ao conhecimento direto, à
iluminação, a Nibbana.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
