sofrimento.’ Um esforço deve ser feito para compreender: ‘Esta é a cessação do
sofrimento.’ Um esforço deve ser feito para compreender: ‘Este é o caminho que
conduz à cessação do sofrimento.’”<o:p></o:p></span></p>


