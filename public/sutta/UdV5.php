<p class=Normal style='margin-left:.75in'>




<p class=Normal>Assim ouvi. Em certa ocasião o Abençoado estava<span style="mso-spacerun:
yes">  </span>em Savatthi, no palácio da mãe de Migara, no Parque do Oriente<span
lang=PT-BR style='mso-ansi-language:PT-BR'>. Naquela ocasião, sendo o dia da
observância, o Abençoado estava sentado circundado pela comunidade de bhikkhus.
Então o Ven. Ananda – quando a noite já havia avançado, ao final da primeira
vigília – levantou-se do seu assento, arrumou o seu manto sobre um ombro, ficou
em pé de frente para o Abençoado com as mãos postas sobre o seu coração e lhe
disse: &quot;A noite, venerável senhor, já avançou bastante. A primeira vigília
terminou. A comunidade dos bhikkhus está sentada aqui há tempo. Que o Abençoado
lhes recite o Patimokkha.&quot; Quando isso foi dito o Abençoado permaneceu em silêncio.
<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então uma segunda vez,
quando a noite já havia avançado bastante, ao final da segunda vigília, o Ven.
Ananda levantou-se do seu assento, arrumou o seu manto sobre um ombro, ficou em
pé de frente para o Abençoado com as mãos postas sobre o seu coração e lhe
disse: &quot;A noite, venerável senhor, já avançou bastante. A segunda vigília
terminou. A comunidade dos bhikkhus está sentada aqui há tempo. Que o Abençoado
lhes recite o Patimokkha.&quot; Quando isso foi dito o Abençoado permaneceu em silêncio.
<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então uma terceira vez,
quando a noite já havia avançado bastante, ao final da terceira vigília, com o
amanhecer surgindo e a noite desaparecendo, o Ven. Ananda levantou-se do seu
assento, arrumou o seu manto sobre um ombro, ficou em pé de frente para o
Abençoado com as mãos postas sobre o seu coração e lhe disse: &quot;A noite,
venerável senhor, já avançou bastante. A terceira vigília terminou. O amanhecer
está surgindo e a noite desaparecendo A comunidade dos bhikkhus está sentada
aqui há tempo. Que o Abençoado lhes recite o Patimokkha.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Ananda, esta
assembléia não é pura.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, o pensamento ocorreu
ao Ven. Maha Moggallana: &quot;Com referência a qual indivíduo o Abençoado
disse agora, ‘Ananda, esta assembléia não é pura’?&quot; Dessa forma ele dirigiu
a sua mente, abrangendo com a sua mente a mente de toda a comunidade de
bhikkhus. Ele viu aquele indivíduo – imoral, mau, sujo e suspeito nas suas
tarefas, oculto em suas ações, que não é um contemplativo, ao contrário do que
diz, que não vive a vida santa, ao contrário do que diz, podre por dentro, esvaindo-se
em desejo, imundo por natureza – sentado no meio da comunidade de bhikkhus.
Vendo-o, ele se levantou e dirigiu-se até o individuo,<span
style="mso-spacerun: yes">  </span>&quot;Levante-se, meu amigo. Você foi visto
pelo Abençoado. Você não tem nenhuma ligação com a comunidade de
bhikkhus.&quot; Então o individuo permaneceu em silêncio. Uma segunda vez...Uma
terceira vez, o Ven. Maha Moggallana disse,<span style="mso-spacerun: yes"> 
</span>&quot;Levante-se, meu amigo. Você foi visto pelo Abençoado. Você não tem
nenhuma ligação com a comunidade de bhikkhus.&quot; E pela terceira vez o
individuo permaneceu em silêncio. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o Ven. Maha
Moggallana, agarrando aquele indivíduo pelo braço, expulsou-o pela porta de
entrada e travando o fecho, aproximou-se do Abençoado e disse, &quot;Eu
expulsei aquele indivíduo, venerável senhor. A assembléia agora está pura. Que
o Abençoado recite o Patimokkha para a comunidade dos bhikkhus&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Não é impressionante,
Moggallana. Não é espantoso, como aquele individuo esperou até que ele fosse
agarrado pelo braço.&quot; Então o Abençoado dirigiu-se aos bhikkhus: &quot;A
partir de agora eu não mais executarei a observância ou recitarei o Patimokkha.
Vocês sozinhos bhikkhus, executarão a observância e recitarão o Patimokkha. É
impossível, não pode acontecer, que um Tathagata execute a observância ou
recite o Patimokkha com uma assembléia impura. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Bhikkhus, existem
esses oito fatos maravilhosos e admiráveis acerca do oceano, que sempre que
os Asuras os vêm, fazem com que eles se deleitem com o oceano. Quais
oito? <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;[1] O oceano tem
bancos de areia graduais, um declive gradual, uma inclinação gradual, com uma
queda abrupta somente após um longo trecho. O fato de que o oceano tem bancos
de areia graduais, um declive gradual, uma inclinação gradual, com uma queda
abrupta somente após um longo trecho: Essa é a primeira qualidade maravilhosa e admirável acerca do oceano que, sempre que os Asuras a vêm, faz com que eles se deleitem com o oceano. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;[2] E além disso, o
oceano é estável e não vai além da linha da maré ... Essa é a segunda qualidade
maravilhosa e admirável acerca do oceano que sempre que os Asuras a vêm, faz
com que eles se deleitem com o oceano. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;[3] E além disso, o
oceano não tolera um cadáver. Todo cadáver no oceano é trazido para a praia e
arremessado sobre a terra firme ... Essa é a terceira qualidade maravilhosa e admirável
acerca do oceano que sempre que os Asuras a vêm, faz com que eles se deleitem com o oceano. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;[4] E além disso,
todos os grandes rios que existem – como o Ganges, o Yamuna, o Aciravati, o
Sarabhu, o Mahi – ao chegar ao oceano, abrem mão dos seus nomes anteriores e
são classificados simplesmente como &quot;oceano&quot; ... Essa é a quarta qualidade
maravilhosa e admirável acerca do oceano que, sempre que os Asuras a vêm,
faz com que eles se deleitem com o oceano. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;[5] E além disso,
apesar dos rios do mundo fluírem para o oceano, e as chuvas caírem do céu,
nenhum inchaço ou redução no oceano pode ser notado por essa razão ... Essa é a
quinta qualidade maravilhosa e admirável acerca do oceano que, sempre que os
Asuras a vêm, faz com que eles se deleitem com o oceano. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;[6] E além disso, o
oceano tem um único sabor: o do sal ... Essa é a sexta qualidade maravilhosa e admirável acerca do oceano que sempre que os Asuras a vêm, faz com que eles se deleitem com o oceano. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;[7] E além disso, o
oceano tem muitos tesouros de vários tipos: pérolas, safiras, lápis lazuli,
conchas, quartzo, coral, prata, ouro, rubis ... Essa é a sétima qualidade
maravilhosa e admirável acerca do oceano que sempre que os Asuras a vêm, faz
com que eles se deleitem com o oceano. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;[8] E além disso, o
oceano é a residência de seres poderosos como as baleias, os devoradores de
baleias e os devoradores dos devoradores de baleias; asuras, nagas, e
gandhabbas. Existem no oceano seres com comprimento de cem léguas, duzentas ... trezentas ... quatrocentas ... quinhentas
léguas. O fato de que o oceano é a residência de seres poderosos como as
baleias, os devoradores de baleias e os devoradores dos devoradores de
baleias; asuras, nagas, e gandhabbas e existirem no oceano seres com comprimento
de cem léguas, duzentas ... trezentas ... quatrocentas ... quinhentas léguas: Essa é a
oitava qualidade maravilhosa e admirável acerca do oceano que, sempre que os
Asuras a vêm, faz com que eles se deleitem com o oceano. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Esses são as oito
qualidades maravilhosas e admiráveis acerca do oceano que, sempre que os Asuras as
vêm, fazem com que eles se deleitem com o oceano. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Da mesma forma,
bhikkhus existem oito qualidades maravilhosas e admiráveis acerca desta Doutrina e Disciplina
que, sempre que os Bhikkhus as vêm, fazem com que eles se deleitem com
esta Doutrina e Disciplina. Quais oito? <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;[1] Da mesma forma
como o oceano tem bancos de areia graduais, um declive gradual, uma inclinação
gradual, com uma queda abrupta somente após um longo trecho, assim também
esta Doutrina e Disciplina tem um treinamento gradual, um desempenho gradual,
uma progressão gradual, com uma penetração do conhecimento somente após um
longo percurso. O fato de que esta Doutrina e Disciplina tem um treinamento
gradual, um desempenho gradual, uma progressão gradual, com a penetração do
conhecimento somente após um longo percurso: esta é a primeira qualidade
maravilhosa e admirável acerca desta Doutrina e Disciplina e, sempre que os Bhikkhus a
vêm, faz com que eles se deleitem com esta Doutrina e Disciplina.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;[2] E além disso, da
mesma forma como o oceano é estável e não vai além a linha da maré, assim também meus discípulos – mesmo pela segurança das suas próprias vidas – não vão
além das regras de treinamento que eu lhes formulei ... esta é a segunda qualidade
maravilhosa e admirável acerca desta Doutrina e Disciplina e, sempre que os
Bhikkhus a vêm, faz com que eles se deleitem com esta Doutrina e
Disciplina.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;[3] E além disso, da
mesma forma como o oceano não tolera um cadáver - todo cadáver no oceano é
trazido para a praia e arremessado sobre a terra firme – assim também, se um
indivíduo é imoral, mau, sujo e suspeito nas suas tarefas, oculto em suas
ações, não é um contemplativo ao contrário do que diz, não vive a vida santa ao
contrário do que diz, podre por dentro, esvaindo-se em desejo, imundo por
natureza – a comunidade não tem comunhão com ele. Organizando imediatamente uma
reunião, eles o banem da comunidade. Embora ele possa estar sentado no meio da
comunidade, ele está muito distante da comunidade, e a comunidade distante
dele ... esta é a terceira qualidade maravilhosa e admirável acerca desta Doutrina e
Disciplina e, sempre que os Bhikkhus a vêm, faz com que eles se deleitem
com esta Doutrina e Disciplina.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;[4] E além disso, da
mesma forma
como todos os grandes rios que existam – como o Ganges, o Yamuna, o Aciravati,
o Sarabhu, o Mahi – ao chegar ao oceano, abrem mão dos seus nomes anteriores e
são classificados simplesmente como &quot;oceano&quot;, assim também, quando os
membros das quatro castas – nobres guerreiros, brâmanes, comerciantes e
trabalhadores - adotam a vida santa com a Doutrina e Disciplina declarada pelo
Tathagata, eles abrem mão de seus nomes e clãs anteriores e são classificados
simplesmente como &quot;os contemplativos, filhos do Sakya&quot; ... esta é a
quarta qualidade maravilhosa e admirável acerca desta Doutrina e Disciplina e,
sempre que os Bhikkhus a vêm, faz com que eles se deleitem com esta Doutrina
e Disciplina.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;[5] E além disso, da
mesma forma
como os rios do mundo fluem para o oceano, e as chuvas caem do céu, sem que
nenhum inchaço ou redução no oceano possa ser notado por essa razão, assim também,
apesar de que muitos bhikkhus estão totalmente libertados com as propriedades
do Ilimitado sem que lhes reste nenhum apego, nenhum inchaço ou redução
nas propriedades do Ilimitado pode ser notado por essa razão ... esta é a quinta
qualidade maravilhosa e admirável acerca desta Doutrina e Disciplina e, sempre
que os Bhikkhus a vêm, faz com que eles se deleitem com esta Doutrina e
Disciplina.<o:p></o:p></span></p>

<p class=Normal><a name=sabor></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;[6]
E além disso, da
mesma forma como o oceano tem um único sabor – aquele do sal – assim também,
esta Doutrina e Disciplina tem um único sabor: aquele da libertação ... esta é a
sexta qualidade maravilhosa e admirável acerca desta Doutrina e Disciplina e,
sempre que os Bhikkhus a vêm, faz com que eles se deleitem com esta
Doutrina e Disciplina.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;[7] E além disso, da
mesma forma
como o oceano tem muitos tesouros de vários tipos - pérolas, safiras, lápis
lazuli, conchas, quartzo, coral, prata, ouro, rubis – assim também, esta
Doutrina e Disciplina tem muitos tesouros de vários tipos: os quatro
fundamentos da atenção plena, os quatro esforços corretos, as quatro bases do
poder, as cinco faculdades, os cinco poderes, os sete fatores da Iluminação, o
nobre caminho óctuplo ... esta é a sétima qualidade maravilhosa e admirável acerca
desta Doutrina e Disciplina e, sempre que os Bhikkhus a vêm, faz com que eles se deleitem com esta Doutrina e Disciplina.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;[8] E além disso, da
mesma forma
como o oceano é a residência de seres poderosos como as baleias, os devoradores
de baleias e os devoradores dos devoradores de baleias; asuras, nagas, e
gandhabbas e existam no oceano seres com comprimento de cem léguas,
duzentas ... trezentas ... quatrocentas ... quinhentas léguas; assim também, esta
Doutrina e Disciplina é a residência de seres poderosos como aqueles 'que
entraram na correnteza’ e aqueles que praticam para realizar os frutos do
estado dos ‘que entraram na correnteza'; os ‘que retornarão apenas uma vez
mais’ e aqueles que praticam para realizar os frutos do estado dos ‘que
retornarão apenas uma vez mais'; os ‘que não retornarão mais’ e aqueles que
praticam para realizar os frutos do estado dos ‘que não retornarão mais’; os
Arahants e aqueles que praticam para realizar os frutos do estado de arahant. O
fato de que esta Doutrina e Disciplina é a residência de seres poderosos como
aqueles 'que entraram na correnteza’ e aqueles que praticam para realizar os
frutos do estado dos 'que entraram na correnteza'; os ‘que retornarão apenas
uma vez mais’ e aqueles que praticam para realizar os frutos do estado dos ‘que
retornarão apenas uma vez mais'; os ‘que não retornarão mais’ e aqueles que
praticam para realizar os frutos do estado dos ‘que não retornarão mais’; os
Arahants e aqueles que praticam para realizar os frutos do estado de arahant:
esta é a oitava qualidade maravilhosa e admirável acerca desta Doutrina e
Disciplina e, sempre que os Bhikkhus a vêm, faz com que eles se deleitem com esta Doutrina e Disciplina.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Essas são as oito
qualidades maravilhosas e admiráveis acerca desta Doutrina e Disciplina e, sempre
que os Bhikkhus as vêm, fazem com que eles se deleitem com esta
Doutrina e Disciplina.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, dando-se conta do
significado disso, o Abençoado nessa ocasião exclamou: <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:PT-BR'>A
chuva encharca o que está coberto,<br>
e não encharca o que está exposto.<br>
Dessa forma exponham o que está coberto,<br>
para que não se encharque com a chuva. <o:p></o:p></span></p>

