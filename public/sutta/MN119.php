<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião, o Abençoado estava em Savatthi no Bosque de Jeta, no
Parque de Anathapindika.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.
Então,<span style='mso-spacerun:yes'>  </span>inúmeros bhikkhus estavam
sentados no salão de reuniões, onde eles se reuniram após a refeição, depois de
terem esmolado alimentos, quando esta discussão teve início: “É maravilhoso,
amigos, é admirável, como foi dito pelo Abençoado que sabe e vê, um arahant, perfeitamente iluminado, que a atenção plena no corpo, quando desenvolvida e
cultivada, produz grandes frutos e grandes benefícios.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>No entanto,
essa conversa foi interrompida pois o Abençoado, ao anoitecer, levantou da
meditação e foi até o salão de reuniões e sentou num assento que havia sido
preparado. Então, ele se dirigiu aos bhikkhus assim:<span
style='mso-spacerun:yes'>  </span>“Bhikkhus, por qual discussão vocês estão
reunidos aqui agora? E qual foi a conversa que foi interrompida?”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
venerável senhor, estávamos sentados no salão de reuniões, onde nos reunimos
após a refeição, depois de termos esmolado alimentos, quando esta discussão
teve início: ‘É maravilhoso, amigos, é admirável, como foi dito pelo Abençoado
que sabe e vê, um arahant, perfeitamente iluminado, que a atenção plena no corpo,
quando desenvolvida e cultivada, produz grandes frutos e grandes benefícios.’
Essa era a nossa discussão, venerável senhor, que foi interrompida quando o
Abençoado chegou.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. “E como,
bhikkhus, é desenvolvida a atenção plena no corpo para que produza grandes
frutos e grandes benefícios?<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(ATENÇÃO
PLENA NA RESPIRAÇÃO)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. Aqui, um
bhikkhu, [<a href="#N1">1</a>] <a name=R1></a>dirigindo-se à floresta, ou à
sombra de uma árvore, ou a um local isolado; senta-se com as pernas cruzadas,
mantém o corpo ereto e estabelecendo a plena atenção à sua frente, ele inspira
com atenção plena justa, ele expira com atenção plena justa. Inspirando longo,
ele compreende : ‘Eu inspiro longo’; ou expirando longo, ele compreende: ‘Eu
expiro longo.’ Inspirando curto, ele compreende: ‘Eu inspiro curto’; ou
expirando curto, ele compreende: ‘Eu expiro curto.’ <a name=R8></a>Ele treina
assim: ‘Eu inspiro experienciando todo o corpo [da respiração]’; ele treina
assim: ‘Eu expiro experienciando todo o corpo [da respiração].’ <a name=R9></a>Ele
treina assim: ‘Eu inspiro tranqüilizando a formação do corpo [da respiração]’: ele treina assim: 
‘Eu expiro tranqüilizando a formação do corpo [da
respiração].’ <a name=R10></a>Assim como um torneiro habilidoso ou seu
aprendiz, quando faz uma volta longa, compreende: ‘Eu faço uma volta longa’;
ou, quando faz uma volta curta, compreende: ‘Eu faço uma volta curta’; da mesma
forma, inspirando longo, um Bhikkhu compreende: ‘Eu inspiro longo’...ele
treina assim: ‘Eu expiro tranqüilizando a formação do corpo.’ Enquanto ele
assim permanece diligente, ardente e decidido, as memórias e intenções baseadas
na vida em família são abandonadas; com o seu abandono a sua mente se
estabiliza no seu interior, se acalma, é trazida para a unicidade e fica concentrada.
Assim é como um bhikkhu desenvolve a atenção plena no corpo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(AS QUATRO
POSTURAS)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5.
&quot;Novamente, bhikkhus, quando caminhando, um bhikkhu compreende: ‘Eu estou
caminhando’; quando em pé, ele compreende: ‘Eu estou em pé’; quando sentado, ele
compreende: ‘Eu estou sentado’; quando deitado, ele compreende: ‘Eu estou
deitado’; ou ele compreende a postura do corpo conforme for o caso. Enquanto
ele assim permanece diligente, ardente e decidido, as memórias e intenções
baseadas na vida em família são abandonadas ... Assim também é como um bhikkhu
desenvolve a atenção plena no corpo.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(PLENA
CONSCIÊNCIA)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6.
&quot;Novamente, bhikkhus, um bhikkhu age com plena consciência ao ir para a
frente e retornar; <a name=R15></a>age com plena consciência ao olhar para
frente e desviar o olhar; age com plena consciência ao dobrar e estender os
membros; age com plena consciência ao carregar o manto externo, o manto
superior, a tigela; age com plena consciência ao comer, beber, mastigar e
saborear; age com plena consciência ao urinar e defecar; age com plena
consciência ao caminhar, ficar em pé, sentar, dormir, acordar, falar e
permanecer em silêncio. Enquanto ele assim permanece diligente, ardente e
decidido, as memórias e intenções baseadas na vida em família são abandonadas
... <span style='mso-spacerun:yes'> </span>Assim também é como um bhikkhu
desenvolve a atenção plena no corpo.<o:p></o:p></span></p>



<p class=Normal><a name="repulsa"></a><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(REPULSA
– AS PARTES DO CORPO)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7.
&quot;Novamente, bhikkhus, um bhikkhu examina esse mesmo corpo para cima, a
partir da sola dos pés, e para baixo, a partir do topo dos cabelos limitado pela
pele e repleto de muitos tipos de coisas repulsivas, portanto: ‘Neste corpo existem
cabelos, pêlos do corpo, unhas, dentes, pele, carne, tendões, ossos, tutano,
rins, coração, fígado, diafragma, baço, pulmões, intestino grosso, intestino
delgado, conteúdo do estômago, fezes, bílis, fleuma, pus, sangue, suor,
gordura, lágrimas, saliva, muco, líquido sinovial, e urina.’<a name=R16></a> Como
se houvesse um saco com uma abertura em uma extremidade, cheio de vários tipos
de grãos, como arroz sequilho, arroz vermelho, feijões, ervilhas, milhete e
arroz branco, e um homem com vista boa o abrisse e examinasse: ‘Isto é arroz
sequilho, arroz vermelho, feijões, ervilhas, milhete e arroz branco’; do mesmo
modo um bhikkhu examina esse mesmo corpo ... repleto de muitos tipos de
coisas repulsivas: ‘ Neste corpo existem cabelos ... e urina.’ Enquanto ele assim
permanece diligente, ardente e decidido, as memórias e intenções baseadas na
vida em família são abandonadas ... Assim também é como um bhikkhu desenvolve a
atenção plena no corpo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(ELEMENTOS)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8.
&quot;Novamente, bhikkhus, um bhikkhu examina esse mesmo corpo que, não
importando sua posição ou postura, consiste de elementos da seguinte forma:
‘Neste corpo existe o elemento terra, o elemento água, o elemento fogo e o
elemento ar.’ <a name=R17></a>Do mesmo modo, como se um açougueiro habilidoso ou seu aprendiz 
tivesse matado uma vaca e estivesse sentado numa
encruzilhada com a vaca em pedaços; assim também um bhikkhu examina esse mesmo
corpo que<span style='mso-spacerun:yes'>  </span>... consiste de elementos, portanto:
‘ Neste corpo existe o elemento terra, o elemento água, o elemento fogo e o
elemento ar.’ Enquanto ele assim permanece diligente, ardente e decidido, as
memórias e intenções baseadas na vida em família são abandonadas ... Assim
também é como um bhikkhu desenvolve a atenção plena no corpo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(AS NOVE
CONTEMPLAÇÕES DO CEMITÉRIO)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9.
&quot;Novamente, bhikkhus, como se ele visse um cadáver jogado num cemitério,
um, dois ou três dias depois de morto, inchado, lívido e esvaindo matéria, um
bhikkhu compara seu mesmo corpo com ele: ‘Este corpo também tem a mesma
natureza, se tornará igual, não está isento desse destino.’<span
style='mso-spacerun:yes'>  </span>Enquanto ele assim permanece diligente ...
Assim também é como um bhikkhu desenvolve a atenção plena no corpo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10.
&quot;Novamente, como se ele visse um cadáver jogado num cemitério, sendo
devorado por corvos, gaviões, abutres, cães, chacais ou vários tipos de vermes,
um bhikkhu compara o seu corpo com ele: ‘Este corpo também tem a mesma
natureza, se tornará igual, não está isento desse destino. Enquanto ele assim
permanece diligente ... Assim também é como um bhikkhu desenvolve a atenção
plena no corpo.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11-14.
&quot;Novamente, bhikkhus, como se ele visse um cadáver jogado num cemitério,
um esqueleto com carne e sangue, que se mantém unido por tendões ... um
esqueleto descarnado lambuzado de sangue, que se mantém unido por tendões ...
um esqueleto descarnado e sem sangue que se mantém unido por tendões ... ossos
desconectados espalhados em todas as direções – aqui um osso da mão, ali um
osso do pé, aqui um osso da perna, ali um osso da coxa, aqui um osso da bacia,
ali um osso da coluna vertebral, aqui uma costela, ali um osso do peito, aqui
um osso do braço, ali um osso do ombro, aqui um osso do pescoço, ali um osso da
mandíbula, aqui um dente, ali um crânio - um Bhikkhu compara o seu corpo com
ele, portanto: ‘ Este corpo também tem a mesma natureza, se tornará igual, não
está isento desse destino.’ Enquanto ele assim permanece diligente ... Assim
também é como um bhikkhu desenvolve a atenção plena no corpo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15-17.
&quot;Novamente, bhikkhus, como se ele visse um cadáver jogado num cemitério,
os ossos brancos desbotados, cor de conchas ... ossos amontoados, com mais de
um ano ... ossos apodrecidos e esfarelados convertidos em pó, um bhikkhu
compara o seu corpo com ele, portanto: ‘ Este corpo também tem a mesma
natureza, se tornará igual, não está isento desse destino.’<span
style='mso-spacerun:yes'>  </span>Enquanto ele assim permanece diligente ...
Assim também é como um bhikkhu desenvolve a atenção plena no corpo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(OS JHANAS)</b><o:p></o:p></span></p>



<p class=Normal style='mso-layout-grid-align:none;punctuation-wrap:simple;
text-autospace:none'><span lang=PT-BR style='mso-ansi-language:PT-BR'>18.
&quot;Novamente, bhikkhus, um bhikkhu afastado dos prazeres sensuais, afastado das qualidades não hábeis, entra e permanece no primeiro jhana, que é caracterizado pelo pensamento aplicado e sustentado, com o êxtase e felicidade nascidos do afastamento. Ele permeia e impregna, cobre e preenche o corpo com o êxtase e felicidade nascidos do afastamento. Não há nada em todo o corpo que não esteja permeado pelo êxtase e felicidade nascidos do afastamento. É como se
um banhista habilidoso ou seu aprendiz [</span><a href="#N2"><span lang=PT-BR
style='mso-ansi-language:PT-BR'>2</span></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>] <a name=R2></a>vertesse pó de banho numa bacia de latão e o misturasse, borrifando com água de tempos em tempos, de forma que essa bola de pó de banho - saturada, carregada de umidade, permeada por dentro e por fora - no entanto não pingasse; assim, o bhikkhu permeia, cobre e preenche o corpo com o êxtase e felicidade nascidos do afastamento.... Enquanto ele assim permanece
diligente ... Assim também é como um bhikkhu desenvolve a atenção plena no
corpo.<o:p></o:p></span></p>



<p class=Normal style='mso-layout-grid-align:none;punctuation-wrap:simple;
text-autospace:none'><span lang=PT-BR style='mso-ansi-language:PT-BR'>19.
&quot;Novamente, bhikkhus, abandonando o pensamento aplicado e sustentado, um bhikkhu entra e permanece no segundo jhana, que é caracterizado pela segurança interna e perfeita unicidade da mente, sem o pensamento aplicado e sustentado, com o êxtase e felicidade nascidos da concentração. Ele permeia e impregna, cobre e preenche o corpo com o êxtase e felicidade nascidos da concentração. Não há nada em todo o corpo que não esteja permeado pelo êxtase e felicidade nascidos da concentração. Como um lago sendo alimentado por uma fonte de água interna, não tendo um fluxo de água do leste, oeste, norte, ou sul, nem os céus periodicamente fornecendo chuvas abundantes, de modo que a fonte de água interna permeia e impregna, cobre e preenche o lago de água fresca, sem que nenhuma parte do lago não esteja permeada pela água fresca; assim também o bhikkhu permeia e impregna, cobre e preenche o corpo com o êxtase e felicidade nascidos da concentração... Enquanto ele assim permanece diligente ... Assim também é como um bhikkhu desenvolve a atenção plena no corpo.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>20.
&quot;Novamente, bhikkhus, abandonando o êxtase, um bhikkhu entra e permanece no terceiro jhana que é caracterizado pela felicidade sem o êxtase, acompanhada pela atenção plena, plena consciência e equanimidade, acerca do qual os nobres declaram: ‘Ele permanece numa estada feliz, equânime e plenamente atento.’ Ele permeia e impregna, cobre e preenche o corpo com a felicidade despojada do êxtase, de forma que não exista nada em todo o corpo que não esteja permeado com a felicidade despojada do êxtase. Como num lago que tenha flores de lótus azuis, brancas ou vermelhas, podem existir algumas flores de lótus azuis, brancas, ou vermelhas que, nascidas e tendo crescido na água, permanecem imersas na água e florescem sem sair de dentro da água, de forma que elas permanecem permeadas e impregnadas, cobertas e preenchidas com água fresca da raiz até a ponta, e nada dessas flores de lótus azuis, brancas ou vermelhas permanece sem estar permeado pela água fresca; assim também o bhikkhu permeia e impregna, cobre e preenche o corpo com a felicidade despojada de êxtase ...  Enquanto ele assim permanece diligente ... Assim também é como um bhikkhu desenvolve a atenção plena no corpo.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>21.
&quot;Novamente, bhikkhus, com o completo desaparecimento da felicidade, um bhikkhu entra e permanece no quarto jhana, que possui nem felicidade nem sofrimento, com a atenção plena e a equanimidade purificadas. Ele permanece permeando o corpo com a mente pura e luminosa, de forma que não exista nada em todo o corpo que não esteja permeado pela mente pura e luminosa. Como se um homem estivesse enrolado da cabeça aos pés com um tecido branco de forma que não houvesse nenhuma parte do corpo que não estivesse coberta pelo tecido branco; assim também o bhikkhu permanece permeando o corpo com a mente pura e luminosa. Não há nada no corpo que não esteja permeado por essa mente pura e luminosa. Enquanto ele assim permanece diligente ... Assim também é como um bhikkhu desenvolve a atenção plena no corpo.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(PROGRESSO
ATRAVÉS DA ATENÇÃO PLENA NO CORPO)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>22.
&quot;Bhikkhus, qualquer um que tenha desenvolvido e cultivado a atenção plena
no corpo, incluiu dentro de si mesmo todos os estados hábeis que existem, que
compartilham do verdadeiro conhecimento: [</span><a href="#N3"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>3</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>] <a name=R3></a>Como alguém, que tenha estendido
a sua mente sobre o grande oceano, terá incluído dentro dela todos os rios que
fluem para aquele oceano; assim também qualquer um que tenha desenvolvido e
cultivado a atenção plena no corpo, incluiu dentro de si mesmo todos os estados
hábeis que existem, que compartilham do verdadeiro conhecimento.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>23.
&quot;Bhikkhus, se uma pessoa não tiver desenvolvido e cultivado a atenção
plena no corpo, Mara encontrará uma oportunidade e um suporte nela. Suponham
que um homem arremessasse uma pedra pesada sobre um monte de argila molhada. O
que vocês pensam, bhikkhus? Aquela pedra pesada penetraria dentro daquele monte
de argila molhada?” – “Sim, venerável senhor” – “Do mesmo modo, bhikkhus, se
uma pessoa não tiver desenvolvido e cultivado a atenção plena no corpo, Mara
encontrará uma oportunidade e um suporte nela.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>24.
“Suponha que houvesse um pedaço de madeira seca e viesse um homem com um
pau para acender um fogo, pensando: ‘Eu vou acender um fogo, eu irei produzir
calor.’ O que vocês pensam, bhikkhus? Aquele homem conseguiria acender um fogo
e produzir calor esfregando o pau naquele pedaço de madeira seca?” – “Sim,
venerável senhor.” – “Do mesmo modo, bhikkhus, se uma pessoa não tiver
desenvolvido e cultivado a atenção plena no corpo, Mara encontrará uma oportunidade
e um suporte nela.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>25.
“Suponha que houvesse um jarro vazio sobre um suporte e um homem viesse com um
suprimento de água. O que vocês pensam, bhikkhus? O homem poderia encher o
jarro com água?” – “Sim, venerável senhor.” –<span style='mso-spacerun:yes'> 
</span>“Do mesmo modo, bhikkhus, se uma pessoa<span style='mso-spacerun:yes'> 
</span>não tiver desenvolvido e cultivado a atenção plena no corpo, Mara
encontrará uma oportunidade e um suporte nela.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>26.
&quot;Bhikkhus, se uma pessoa tiver desenvolvido e cultivado a atenção plena no
corpo, Mara não encontrará uma oportunidade e um suporte nela. Suponham que um
homem arremessasse uma bola leve, de linha, numa porta feita de madeira sólida.
O que vocês pensam, bhikkhus? Aquela bola leve, de linha, seria capaz de
penetrar aquela porta feita de madeira sólida?” – “Não, venerável senhor.” –
“Do mesmo modo, bhikkhus, se uma pessoa tiver desenvolvido e cultivado a
atenção plena no corpo, Mara não encontrará uma oportunidade e um suporte
nela.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>27.
“Suponha que houvesse um pedaço de madeira verde úmida e viesse um homem com
um pau para acender um fogo, pensando: ‘Eu vou acender um fogo, eu irei produzir
calor.’ O que vocês pensam, bhikkhus? Aquele homem conseguiria acender um fogo
e produzir calor esfregando o pau naquele pedaço de madeira verde úmida?”<span
style='mso-spacerun:yes'>  </span>- “Não, venerável senhor.” – “Da mesma forma,
bhikkhus, se uma pessoa tiver desenvolvido e cultivado a atenção plena no
corpo, Mara não encontrará uma oportunidade e um suporte nela.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>28.
“Suponha que houvesse, sobre um suporte, um jarro com água até a borda, de tal
forma que corvos pudessem beber nele, e um homem viesse com um suprimento de
água. O que vocês pensam, bhikkhus? O homem poderia colocar água no jarro?”
-<span style='mso-spacerun:yes'>  </span>“Não, venerável  senhor.” – “Da mesma
forma, bhikkhus, se uma pessoa tiver desenvolvido e cultivado a atenção plena
no corpo, Mara não encontrará uma oportunidade e um suporte nela.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>29.
&quot;Bhikkhus, se uma pessoa tiver desenvolvido e cultivado a atenção plena no
corpo, e ela inclinar a sua mente para a realização de qualquer estado que
possa ser realizado por meio do conhecimento direto, ela terá habilidade para
experimentar qualquer aspecto desse estado, pois existe uma base adequada.
Suponha que houvesse, sobre um suporte, um jarro com água até a borda de tal
forma que corvos pudessem beber nele. Sempre que algum homem forte entornasse o
jarro sairia água?” – “Sim, venerável senhor.” – “Da mesma forma, bhikkhus,
se uma pessoa tiver desenvolvido e cultivado a atenção plena no corpo, então ao
inclinar a sua mente para a realização de qualquer estado que possa ser realizado
por meio do conhecimento direto, ela terá habilidade para experimentar qualquer
aspecto desse estado, pois existe uma base adequada.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>30.
“Suponha que houvesse um reservatório quadrado num terreno plano, circundado
por um dique cheio de água até a borda, de tal forma que corvos pudessem beber
nele. Sempre que algum homem forte afrouxasse o dique sairia água?” - “Sim,
venerável senhor.” – “Da mesma forma, bhikkhus, se uma pessoa tiver
desenvolvido e cultivado a atenção plena no corpo … ela terá habilidade para
experimentar qualquer aspecto desse estado, pois existe uma base adequada.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>31.
“Suponha que houvesse uma carruagem num terreno plano numa encruzilhada, com
puros-sangues arreados e a aguilhada preparada, de forma que um adestrador habilidoso,
um cocheiro domador de cavalos pudesse montar nela e, tomando as rédeas na mão
esquerda e a aguilhada na mão direita, pudesse ir e regressar por qualquer
caminho quando ele quisesse. Da mesma forma, bhikkhus, se uma pessoa tiver
desenvolvido e cultivado a atenção plena no corpo … ela terá habilidade para
experimentar qualquer aspecto desse estado, pois existe uma base adequada.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(BENEFÍCIOS
DA ATENÇÃO PLENA NO CORPO)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>32.
“Bhikkhus, quando a atenção plena no corpo tiver sido praticada repetidas
vezes, desenvolvida, cultivada, feito um veículo, uma base, estabilizada,
exercitada e aperfeiçoada, estes dez benefícios podem ser esperados. Quais dez?<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>33. (i)
“Ele se torna um conquistador do descontentamento e do deleite, e o
descontentamento não o conquista, ele permanece subjugando o descontentamento
sempre que este surgir.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>34. (ii)
“Ele se torna um conquistador do medo e do terror, e o medo e o terror não o
conquistam, ele permanece subjugando o medo e o terror sempre que estes
surgirem.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>35. (iii)
“Ele suporta o frio e o calor, fome e sede e o contato com moscas, mosquitos,
vento, sol e coisas rastejantes; ele suporta palavras hostis e indesejáveis e
sensações corporais que são dolorosas, torturantes, penetrantes, cortantes,
desagradáveis, agonizantes e que ameaçam a vida.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>36. (iv)
“Ele obtém de acordo com a sua vontade, sem problemas ou dificuldades, os
quatro jhanas que constituem a mente superior e proporcionam um estado
prazeroso aqui e agora.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>37. (v)
“Ele exerce os vários tipos de poderes supra-humanos... (igual ao </span><a
href="MN108.php#T18"><span lang=PT-BR style='mso-ansi-language:PT-BR'>MN108,
verso 18</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>) ... ele
exerce poderes corporais até mesmo nos distantes mundos de Brahma.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>38. (vi)
“Com o elemento do ouvido divino, que é purificado e ultrapassa o humano, ele
ouve ambos os tipos de sons, os divinos e os humanos, aqueles distantes bem
como os próximos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>39. (vii)
&quot;Ele compreende as mentes de outros seres, de outras pessoas, abarcando-as
com a sua própria mente. Ele compreende uma mente afetada pelo desejo como
afetada pelo desejo ... (igual ao </span><a href="MN108.php#T20"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>MN108, verso 20</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>) ... uma mente não libertada como
não libertada.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>40. (viii)
“Ele se recorda das suas muitas vidas passadas, isto é, um nascimento, dois
nascimentos ... (igual ao </span><a href="MN51.php#T24"><span lang=PT-BR
style='mso-ansi-language:PT-BR'>MN 51, verso 24</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>) ... Assim ele se recorda das suas muitas
vidas passadas nos seus modos e detalhes.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>41. (ix)
“Por meio do olho divino, que é purificado e ultrapassa o humano, ele vê seres
falecendo e renascendo, inferiores e superiores, bonitos e feios, afortunados e
desafortuna<a name=R14></a>dos. Ele compreende como os seres prosseguem de
acordo com as suas ações.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>42. (x)
“Compreendendo por si mesmo com conhecimento direto, ele, aqui e agora, entra e
permanece na libertação da mente e na libertação através da sabedoria que são
imaculadas, com a destruição de todas as impurezas.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>43.
“Bhikkhus, quando a atenção plena no corpo tiver sido praticada repetidas
vezes, desenvolvida, cultivada, feito um veículo, uma base, estabilizada,
exercitada e aperfeiçoada, esses dez benefícios podem ser esperados.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o
que disse o Abençoado. Os bhikkhus ficaram satisfeitos e contentes com as
palavras do Abençoado. <o:p></o:p></span></p>

