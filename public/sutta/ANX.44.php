<p class=Normal> Certa ocasião o Abençoado estava em Kusinara. Lá ele se dirigiu aos bhikkhus: "Bhikkhus!" </p>

<p class=Normal> "Venerável senhor! Aqueles bhikkhus responderam. O Abençoado disse o seguinte: </p>

<p class=Normal> "Bhikkhus, um bhikkhu que deseja admoestar uma outra pessoa deve examinar a si mesmo com relação a cinco coisas e estabelecer nele mesmo cinco coisas antes de admoestar a outra pessoa. Com relação a quais cinco coisas ele deve examinar a si mesmo? </p>

<p class=Normal> (1) "Bhikkhus, um bhikkhu que deseja admoestar uma outra pessoa deve examinar a si mesmo assim: 'O meu comportamento corporal é puro? Possuo o comportamento corporal que é puro, impecável, e irrepreensível? Essa qualidade existe em mim, ou não?' Se o comportamento corporal do bhikkhus não for puro, e ele não possuir o comportamento corporal que é puro, impecável, e irrepreensível, haverá aqueles que dirão: 'Por favor treine primeiro a si mesmo no comportamento corporal.' Haverá aqueles que lhe dirão isso. </p> 

<p class=Normal> (2) "Outra vez, um bhikkhu que deseja admoestar uma outra pessoa deve examinar a si mesmo assim: 'O meu comportamento verbal é puro? Possuo o comportamento verbal que é puro, impecável, e irrepreensível? Essa qualidade existe em mim, ou não?' Se o comportamento verbal do bhikkhus não for puro, e ele não possuir o comportamento verbal que é puro, impecável, e irrepreensível, haverá aqueles que dirão: 'Por favor treine primeiro a si mesmo no comportamento verbal.' Haverá aqueles que lhe dirão isso. </p>

<p class=Normal> (3) "Outra vez, um bhikkhu que deseja admoestar uma outra pessoa deve examinar a si mesmo assim: 'Eu estabeleci uma mente com amor bondade sem ressentimentos em relação aos meus companheiros bhikkhus? Essa qualidade existe em mim, ou não?' Se o bhikkhu não estabeleceu uma mente com amor bondade sem ressentimentos em relação aos seus companheiros bhikkhus, haverá aqueles que dirão: 'Por favor estabeleça uma mente com amor bondade em relação aos seus companheiros bhikkhus.' Haverá aqueles que lhe dirão isso. </p>

<p class=Normal> (4) "Outra vez, um bhikkhu que deseja admoestar uma outra pessoa deve examinar a si mesmo assim: 'Sou estudado, memorizo e me recordo aquilo que aprendi? Aprendi muito daqueles ensinamentos que são admiráveis no início, admiráveis no meio, admiráveis no final, com o significado e fraseado corretos, que revelam uma vida santa que é completamente perfeita e imaculada? Memorizei esses ensinamentos, os recitei verbalmente, investiguei com a mente e penetrei corretamente com o entendimento? Essa qualidade existe em mim, ou não?' Se o bhikkhu não é estudado ... não penetrou corretamente com o entendimento, haverá aqueles que dirão: 'Por favor primeiro aprenda os ensinamentos.' Haverá aqueles que lhe dirão isso. </p>

<p class=Normal> (5) "Outra vez, um bhikkhu que deseja admoestar uma outra pessoa deve examinar a si mesmo assim: 'Ambos Patimokkhas me foram transmitidos em detalhe, foram bem analisados, bem dominados, bem estabelecidos com relação às suas regras e explicações detalhadas? Essa qualidade existe em mim, ou não?' Se ambos Patimokkhas não foram transmitidos em detalhe ... bem estabelecidos com relação às suas regras e explicações detalhadas, e se quando perguntado: 'Onde o Abençoado disse isso?' ele for incapaz de responder, haverá aqueles que dirão: 'Por favor primeiro aprenda a disciplina.' Haverá aqueles que lhe dirão isso. </p>

<p class=Normal> "É com relação a essas cinco coisas que ele deve examinar a si mesmo. </p>

<p class=Normal> "Quais são as cinco coisas que ele deve estabelecer nele mesmo? Ele deve pensar: '(6) falarei no momento oportuno, não no momento inoportuno; (7) direi a verdade, não mentiras; (8) falarei de modo gentil, não grosseiro; (9) falarei de modo benéfico, não prejudicial; (10) falarei com amor bondade na mente, não com raiva.' Essas são as cinco coisas que ele deve estabelecer nele mesmo. </p>

<p class=Normal> "Bhikkhus, um bhikkhu que deseja admoestar uma outra pessoa deve examinar a si mesmo com relação a essas cinco coisas e estabelecer nele mesmo essas cinco coisas antes de admoestar a outra pessoa."
</p>


