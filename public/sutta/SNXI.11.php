<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, no passado, quando Sakka, o senhor dos devas, era um ser
humano, ele adotou e se impôs sete votos através de cuja prática ele alcançou o
status de Sakka. Quais eram os sete votos?<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(1)
“’Enquanto eu viver, que eu possa sustentar os meus pais.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(2)
“’Enquanto eu viver, que eu possa respeitar os anciãos da família.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(3)
“’Enquanto eu viver, que eu possa falar de maneira gentil.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(4) “’Enquanto
eu viver, que eu possa falar sem causar discórdia.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(5)
“’Enquanto eu viver, que eu possa permanecer em casa com a mente desprovida da
mácula da avareza, espontaneamente generoso, mão aberta, que se delicia com a
renúncia, devotado à caridade, que se delicia em dar e compartir.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(6)
“’Enquanto eu viver, que eu possa dizer a verdade.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(7)
“’Enquanto eu viver, que eu possa estar livre da raiva, e se a raiva surgir em
mim que eu possa dissipá-la com rapidez.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“No passado,
quando Sakka, o senhor dos devas, era um ser humano, ele adotou e se impôs sete
