<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Nota:<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Um grande
número de discursos (dentre os quais o <a href="SNXXXV.189.php">SN XXXV.191; <a href="ANVI.63.php">AN VI.63</a>) argumentam que a
mente é agrilhoada, não por coisas como os cinco agregados ou os objetos dos
seis sentidos, mas pelo ato de cobiça por eles e prazer com eles. Há duas
formas para romper esse grilhão. Uma é focar nas desvantagens da cobiça e
prazer em si mesmos, vendo com clareza o sofrimento que estes produzem na
mente. A outra é analisar os objetos da cobiça e do prazer de tal modo que eles
não mais pareçam dignos de interesse. Essa segunda abordagem é aquela
recomendada neste discurso: quando o Buda fala em “esparramar, demolir e
destruir a forma, (etc.) e deixar de brincar com ela,” ele se refere à prática
de analisar minuciosamente as partes constituintes da forma até que esta não
mais pareça ser um objeto apropriado para a cobiça e o prazer. Quando todos os
agregados puderem ser tratados desta maneira, a mente ficará sem nenhum objeto
condicionado para servir como ponto focal para a cobiça e assim estará
libertada – no mínimo – até o estágio do não retorno. <o:p></o:p></span></p>




