<p class=Normal> “Ao falar de Nanda, bhikkhus, alguém poderia corretamente dizer que ele é de boa família, que ele é forte e belo e muito ardente. [<a href="#N1">1</a>] </p>

<p class=Normal><a name="R1"> </a> “De que outro modo poderia Nanda viver a vida santa perfeita e imaculada exceto guardando as portas dos sentidos, sendo moderado ao comer, cultivando a vigilância e estabelecendo a atenção plena e plena consciência? </p>

<p class=Normal> “Assim, bhikkhus, é como Nanda guarda as portas dos sentidos. Se Nanda tiver que olhar para o leste, ele assim faz apenas depois de considerar tudo muito bem na sua mente: ‘Enquanto estiver olhando para o leste, não permitirei que a cobiça ou a tristeza, ou outros estados ruins e prejudiciais, entrem na minha mente.’ Assim ele tem plena consciência. </p>

<p class=Normal> “Se ele tiver que olhar para o oeste, sul ou norte, ele assim faz apenas depois de considerar tudo muito bem na sua mente: ‘Enquanto estiver olhando para o oeste, sul ou norte, não permitirei que a cobiça ou a tristeza, ou outros estados ruins e prejudiciais, entrem na minha mente.’ Assim ele tem plena consciência. </p>

<p class=Normal> “Assim, bhikkhus, é como Nanda é moderado ao comer. Nesse caso, bhikkhus, Nanda toma o seu alimento não como forma de diversão ou para embriaguez, tampouco com o objetivo de embelezamento e para ser mais atraente, mas somente com o propósito de manter a resistência e continuidade desse corpo, como forma de dar um fim ao desconforto e para auxiliar a vida santa. Considerando: ‘Dessa forma darei um fim às antigas sensações (de fome) sem despertar novas sensações (de comida em excesso) e serei saudável e sem culpa e viverei em comodidade.’ Assim, bhikkhus, é como Nanda é moderado ao comer. </p>

<p class=Normal> “Assim, bhikkhus, é como Nanda cultiva a vigilância. Nesse caso, bhikkhus, Nanda, durante o dia, enquanto estiver caminhando para lá e para cá e sentado, purifica a sua mente dos estados obstrutivos. Na primeira vigília da noite, enquanto estiver caminhando para lá e para cá e sentado, ele purifica a sua mente dos estados obstrutivos. Na segunda vigília da noite ele se deita para dormir, no seu lado direito, na postura do leão com um pé sobre o outro, atento e plenamente consciente, após anotar na sua mente o horário para levantar. Após levantar-se, na terceira vigília da noite, enquanto estiver caminhando para cá e para lá e sentado, ele purifica a sua mente dos estados obstrutivos. Assim, bhikkhus, é como Nanda cultiva a vigilância. </p>

<p class=Normal> “Assim, bhikkhus, é como Nanda estabelece a atenção plena e plena consciência. Nesse caso, bhikkhus, as sensações são compreendidas por Nanda quando surgem, compreendidas enquanto estão presentes, compreendidas quando desaparecem. Os objetos mentais são compreendidos quando surgem, compreendidos enquanto estão presentes, compreendidos quando desaparecem. As percepções são compreendidas quando surgem, compreendidas enquanto estão presentes, compreendidas quando desaparecem. Assim, bhikkhus, é como Nanda estabelece a atenção plena e plena consciência. </p>

<p class=Normal> “De que outro modo poderia Nanda viver a vida santa perfeita e imaculada exceto guardando as portas dos sentidos, sendo moderado ao comer, cultivando a vigilância e estabelecendo a atenção plena e plena consciência? </p>

