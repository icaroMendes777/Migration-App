<p class=Normal> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: "Bhikkhus, o Tathagata, um arahant perfeitamente iluminado, com freqüência tem dois pensamentos: o pensamento de segurança (para os seres) e o pensamento de isolamento.</p>

<p class=Normal>"O Tathagata, bhikkhus, aprecia a não má vontade, se delicia com a não má vontade. Apreciando a não má vontade, se deliciando com a não má vontade, este pensamento ocorre com freqüência: 'Através deste comportamento não causo dano a ninguém de modo algum, quer seja fraco ou forte.'</p>

<p class=Normal>"O Tathagata, aprecia o isolamento, se delicia com o isolamento. Apreciando o isolamento, se deliciando com o isolamento, este pensamento ocorre com freqüência: 'Tudo que é prejudicial foi abandonado.'</p>

<p class=Normal>"Portanto, bhikkhus, eu digo que vocês devem permanecer apreciando a não má vontade, se deliciando com a não má vontade. Permanecendo assim este pensamento irá ocorrer com freqüência: 'Através deste comportamento não causo dano a ninguém de modo algum, quer seja fraco ou forte.'</p>

<p class=Normal>"Bhikkhus, vocês também devem permanecer apreciando o isolamento, se deliciando com o isolamento. Permanecendo assim este pensamento irá ocorrer com freqüência: 'O que é prejudicial? O que não foi abandonado? O que foi abandonado?'" </p>

<p class=Normal style='margin-left:.75in'> O Tathagata, o iluminado, <br>
que suportou o que é difícil de suportar, <br>
tem dois pensamentos: <br>
segurança foi o primeiro pensamento mencionado; <br>
isolamento o segundo.</p>

<p class=Normal style='margin-left:.75in'> Aquele que dissipou a escuridão, que foi além, <br>
o grande sábio <br>
que se tornou um mestre, livre das impurezas, <br>
que cruzou completamente, <br>
liberto com a destruição do desejo -<br>
esse sábio suporta o seu último corpo, <br>
superou o envelhecimento, <br>
tendo deixado Mara para trás.</p>

<p class=Normal style='margin-left:.75in'>Como alguém no pico de uma montanha<br>
vê todas as pessoas abaixo, <br>
assim tendo ascendido ao palácio do Dhamma, <br>
aquele que tudo vê, <br>
vê as pessoas no mundo, <br>
ainda imersas na dor, <br>
subjugadas pelo nascimento e envelhecimento.</p>


