<p class=Normal> Em Savatthi. "Bhikkhus, quando alguém permanece contemplando a gratificação nas coisas que agrilhoam, o desejo aumenta. Com o desejo como condição, o apego surge; com o apego como condição, o ser/existir surge; com o ser/existir como condição, o nascimento surge; com o nascimento como condição, o envelhecimento e morte, lamentação, dor, angústia e desespero surgem. Essa é a origem de toda essa massa de sofrimento. </p>

<p class=Normal> "Suponham, bhikkhus, que houvesse uma muda e de tempos em tempos um homem limparia a área em volta das raizes, de tempos em tempos adubaria a planta, de tempos em tempos aguaria a planta. Sustentada por esse cuidado, nutrida por esse cuidado, a planta cresceria, aumentaria, expandiria. Do mesmo modo, quando alguém permanece contemplando a gratificação nas coisas que agrilhoam, o desejo aumenta ... Essa é a origem de toda essa massa de sofrimento. </p>

<p class=Normal> "Bhikkhus, quando alguém permanece contemplando o perigo nas coisas que agrilhoam, o desejo cessa. Com a cessação do desejo, o apego cessa; com a cessação do apego, o ser/existir cessa; ; com a cessação do ser/existir, o nascimento cessa; com a cessação do nascimento, o envelhecimento e morte, lamentação, dor, angústia e desespero cessam. Essa é a cessação de toda essa massa de sofrimento. </p>

<p class=Normal> "Suponham, bhikkhus, que houvesse uma muda. Então um homem viesse trazendo uma pá e um cesto. Ele cortaria a muda pela raiz, cavaria e arrancaria todas as raízes, até mesmo as mais delgadas. Ele cortaria a muda em pedaços, partiria os pedaços em pedaços menores e os reduziria a lascas. Depois ele secaria as lascas ao vento e ao sol, queimaria essas lascas no fogo e recolheria as cinzas. Tendo feito isso, ele deixaria as cinzas serem levadas pelo vento ou as jogaria na correnteza de um rio. Assim, aquela muda teria sido cortada pela raiz, feita como um tronco de palmeira, eliminada de tal forma que não estará mais sujeita a um a um futuro surgimento. Do mesmo modo quando alguém permanece contemplando o perigo nas coisas que agrilhoam, o desejo cessa ... Essa é a cessação de toda essa massa de sofrimento." </p>

