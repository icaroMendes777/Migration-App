<p class=Normal>“Existem cinco maneiras para subjugar a raiva, através das
quais, quando a raiva surge em um bhikkhu, ele deveria removê-la
completamente. Quais cinco? </p>

<p class=Normal>“Quando surge a raiva em relação a uma pessoa, ele deve desenvolver o amor
bondade para com aquela pessoa. Dessa forma a raiva por aquela pessoa deve ser
removida.</p>

<p class=Normal>“Quando surge a raiva em relação a uma pessoa, ele deve desenvolver a
compaixão para com aquela pessoa ... equanimidade<span style="mso-spacerun:
yes">  </span>para com aquela pessoa … ele deve esquecer ou ignorar aquela
pessoa … Quando surge a raiva em relação a uma pessoa, ele deve dirigir os seus
pensamentos para o fato de que aquela pessoa é o produto do kamma dela: ‘Esse
venerável é o dono do seu kamma, herdeiro do seu kamma, nascido do seu kamma,
atado ao seu kamma e dependente do seu kamma. Qualquer kamma que ele faça, para
o bem ou para o mal, disso ele será o herdeiro.’ Dessa forma a raiva por aquela
pessoa deve ser removida. </p>

<p class=Normal>“Essas são as cinco maneiras para subjugar a raiva, através das quais,
quando a raiva surge em um bhikkhu, ele deveria removê-la completamente.” </p>

