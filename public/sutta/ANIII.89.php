<p class=Normal> "Bhikkhus, há esses três treinamentos. Quais três? O treinamento na virtude superior, o treinamento na mente superior e o treinamento na sabedoria superior. </p>

<p class=Normal> "E qual, bhikkhus, é o treinamento na virtude superior? Aqui, um bhikkhu é virtuoso. Ele permanece contido de acordo com o Patimokkha, perfeito na conduta e na sua esfera de atividades. Temendo a menor falha, ele treina adotando os preceitos de virtude. Isso é chamado o treinamento na virtude superior. </p>

<p class=Normal> "E qual, bhikkhus, é o treinamento na mente superior? Aqui, afastado dos prazeres sensuais, afastado das qualidades não hábeis, um bhikkhu entra e permanece no primeiro jhana ... no segundo jhana ... no terceiro jhana ... no quarto jhana ... Isso é chamado o treinamento na mente superior. </p>

<p class=Normal> (89) "E qual, bhikkhus, é o treinamento na sabedoria superior? Aqui, um bhikkhu compreende como na verdade é: 'Isto é sofrimento', 'Esta é a origem do sofrimento', 'Esta é a cessação do sofrimento', 'Este é o caminho que conduz à cessação do sofrimento.' Isso é chamado o treinamento na sabedoria superior. </p>

<p class=Normal> (90) "E qual, bhikkhus, é o treinamento na sabedoria superior? Aqui, com a completa destruição das impurezas mentais, um bhikkhu permanece em um estado livre de impurezas com a libertação da mente e a libertação através da sabedoria, tendo conhecido e manifestado isso para ele mesmo no aqui e agora. </p>

<p class=Normal> "Esses, bhikkhus, são os três treinamentos."</p>

