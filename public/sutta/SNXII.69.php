<p class=Normal> Assim ouvi. Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no Parque de Anathapindika. </p>

<p class=Normal> “Bhikkhus, o inchaço do oceano faz os rios incharem; o inchaço dos rios faz os riachos incharem; o inchaço dos riachos faz os lagos incharem; o inchaço dos lagos faz as lagoas incharem. Do mesmo modo, o inchaço da ignorância faz as formações volitivas incharem; o inchaço das formações volitivas faz a consciência inchar; o inchaço da consciência faz a mentalidade-materialidade (nome e forma) inchar; o inchaço da mentalidade-materialidade (nome e forma) faz as seis bases incharem; o inchaço das seis bases faz o contato inchar; o inchaço do contato faz a sensação inchar; o inchaço da sensação faz o desejo inchar; o inchaço do desejo faz o apego inchar; o inchaço do apego faz o ser/existir inchar; o inchaço do ser/existir faz a o nascimento inchar; o inchaço do nascimento faz o envelhecimento e morte inchar. </p>

<p class=Normal> “Bhikkhus, o esvaziamento do oceano faz os rios esvaziarem; o esvaziamento dos rios faz os riachos esvaziarem; o esvaziamento dos riachos faz os lagos esvaziarem; o esvaziamento dos lagos faz as lagoas esvaziarem. Do mesmo modo, o esvaziamento da ignorância faz as formações volitivas esvaziarem; o esvaziamento das formações volitivas faz a consciência esvaziar...  o esvaziamento do nascimento faz o envelhecimento e morte esvaziar." </p>

