<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-language:PT-BR'>(I. O alimento para os obstáculos)<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, tal como este corpo, sustentado pelo alimento, subsiste na
dependência do alimento e não subsiste sem alimento, assim também os cinco
obstáculos, sustentados pelo alimento, subsistem na dependência do alimento e
não subsistem sem alimento. [<a href="#N1">1</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“E qual, bhikkhus, é o alimento para o surgimento do <i>desejo sensual </i>que
ainda não surgiu ou para o crescimento e incremento do desejo sensual, uma vez
que este tenha surgido? Há, bhikkhus, o sinal da beleza: [<a href="#N2">2</a>] <a
name=R2></a>dar com freqüência atenção sem sabedoria<span style="mso-spacerun:
yes">  </span>para isso, esse é o alimento para o surgimento do desejo sensual<i>
</i>que ainda não surgiu ou para o crescimento e incremento do desejo sensual,
uma vez que este tenha surgido.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é o alimento para o surgimento da <i>má vontade </i>que ainda não
surgiu ou para o crescimento e incremento da má vontade, uma vez que esta tenha
surgido? Há, bhikkhus, o sinal do repulsivo: [<a href="#N3">3</a>] <a name=R3></a>dar
com freqüência atenção sem sabedoria<span style="mso-spacerun: yes"> 
</span>para isso, esse é o alimento para o surgimento da má vontade<i> </i>que
ainda não surgiu ou para o crescimento e incremento da má vontade, uma vez que
esta tenha surgido.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é o alimento para o surgimento da <i>preguiça e torpor </i>que ainda
não surgiram ou para o crescimento e incremento da preguiça e torpor uma vez
que estes tenham surgido? Há, bhikkhus, a letargia descontente, o
espreguiçamento preguiçoso, a sonolência após a refeição e a preguiça mental:
dar com freqüência atenção sem sabedoria<span style="mso-spacerun: yes"> 
</span>para isso, esse é o alimento para o surgimento da preguiça e torpor que
ainda não surgiram ou para o crescimento e incremento da preguiça e torpor, uma
vez que estes tenham surgido.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é o alimento para o surgimento da <i>inquietação e ansiedade </i>que
ainda não surgiram ou para o crescimento e incremento da inquietação e ansiedade
uma vez que estas tenham surgido? Há, bhikkhus, a mente conturbada: dar com
freqüência atenção sem sabedoria<span style="mso-spacerun: yes">  </span>para
isso, esse é o alimento para o surgimento da inquietação e ansiedade que ainda
não surgiram ou para o crescimento e incremento da inquietação e ansiedade, uma
vez que estas tenham surgido.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é o alimento para o surgimento da <i>dúvida </i>que ainda não surgiu
ou para o crescimento e incremento da dúvida uma vez que esta tenha surgido?
Há, bhikkhus, coisas que são a base da dúvida: dar com freqüência atenção sem
sabedoria<span style="mso-spacerun: yes">  </span>para isso, esse é o alimento
para o surgimento da dúvida que ainda não surgiu ou para o crescimento e
incremento da dúvida, uma vez que esta tenha surgido.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Tal como
este corpo, bhikkhus, sustentado pelo alimento, subsiste na dependência do
alimento e não subsiste sem alimento, assim também os cinco obstáculos,
sustentados pelo alimento, subsistem na dependência do alimento e não subsistem
sem alimento..<o:p></o:p></span></p>



<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-language:PT-BR'>(II. O alimento para os fatores da iluminação)<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
tal como este corpo, sustentado pelo alimento, subsiste na dependência do
alimento e não subsiste sem alimento, assim também os fatores da iluminação,
sustentados pelo alimento, subsistem na dependência do alimento e não subsistem
sem alimento.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é o alimento para o surgimento da <i>atenção plena </i>como fator da
iluminação que ainda não surgiu ou para o crescimento e incremento da atenção
plena como fator da iluminação, uma vez que ela tenha surgido? Há, bhikkhus,
coisas que são a base da atenção plena como fator da iluminação: [<a href="#N4">4</a>]
<a name=R4></a>dar com freqüência atenção com sabedoria<span
style="mso-spacerun: yes">  </span>para isso, esse é o alimento para o
surgimento da atenção plena como fator da iluminação<i> </i>que ainda não surgiu
ou para o crescimento e incremento da atenção plena como um fator da
iluminação, uma vez que ela tenha surgido.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><a name="investigacao"> </a>“E qual,
bhikkhus, é o alimento para o surgimento da <i>investigação dos fenômenos </i>como
fator da iluminação que ainda não surgiu ou para o crescimento e incremento da
investigação dos fenômenos como fator da iluminação, uma vez que ela tenha
surgido? Há, bhikkhus, qualidades mentais que são hábeis e inábeis, benéficas e
prejudiciais, superiores e inferiores, claras e escuras com as suas contrapartidas:
[<a href="#N5">5</a>] <a name=R5></a>dar com freqüência atenção com
sabedoria<span style="mso-spacerun: yes">  </span>para isso, esse é o alimento
para o surgimento da investigação dos fenômenos como um fator da iluminação<i> </i>que
ainda não surgiu ou para o crescimento e incremento da investigação dos
fenômenos como fator da iluminação, uma vez que ela tenha surgido.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é o alimento para o surgimento da <i>energia </i>como fator da
iluminação que ainda não surgiu ou para o crescimento e incremento da energia
como fator da iluminação, uma vez que ela tenha surgido? Há, bhikkhus, o
elemento do estímulo, o elemento do empenho, o elemento do esforço: [<a
href="#N6">6</a>] <a name=R6></a>dar com freqüência atenção com sabedoria<span
style="mso-spacerun: yes">  </span>para isso, esse é o alimento para o
surgimento da energia como um fator da iluminação<i> </i>que ainda não surgiu
ou para o crescimento e incremento da energia como fator da iluminação, uma vez
que ela tenha surgido.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é o alimento para o surgimento do <i>êxtase </i>como fator da iluminação
que ainda não surgiu ou para o crescimento e incremento do êxtase como fator da
iluminação, uma vez que este tenha surgido? Há, bhikkhus, coisas que são a base
do êxtase como fator da iluminação: dar com freqüência atenção com sabedoria<span
style="mso-spacerun: yes">  </span>para isso, esse é o alimento para o
surgimento do êxtase como um fator da iluminação<i> </i>que ainda não surgiu ou
para o crescimento e incremento do êxtase como fator da iluminação, uma vez que
ele tenha surgido.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é o alimento para o surgimento da <i>tranqüilidade </i>como fator da
iluminação que ainda não surgiu ou para o crescimento e incremento da
tranqüilidade como fator da iluminação, uma vez que ela tenha surgido? Há,
bhikkhus, a calma corporal e a tranqüilidade mental: [<a href="#N7">7</a>]
<a name=R7></a>dar com freqüência atenção com sabedoria<span
style="mso-spacerun: yes">  </span>para isso, esse é o alimento para o
surgimento da tranqüilidade como um fator da iluminação<i> </i>que ainda não
surgiu ou para o crescimento e incremento da tranqüilidade como fator da
iluminação, uma vez que ela tenha surgido.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é o alimento para o surgimento da <i>concentração </i>como fator da
iluminação que ainda não surgiu ou para o crescimento e incremento da
concentração como fator da iluminação, uma vez que ela tenha surgido? Há,
bhikkhus, o sinal da tranqüilidade, o sinal da não dispersão: [<a href="#N8">8</a>]
<a name=R8></a>dar com freqüência atenção com sabedoria<span
style="mso-spacerun: yes">  </span>para isso, esse é o alimento para o
surgimento da concentração como um fator da iluminação<i> </i>que ainda não
surgiu ou para o crescimento e incremento da concentração como fator da
iluminação, uma vez que ela tenha surgido.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é o alimento para o surgimento da <i>equanimidade </i>como fator da
iluminação que ainda não surgiu ou para o crescimento e incremento da
equanimidade como fator da iluminação, uma vez que ela tenha surgido? Há,
bhikkhus, coisas que são a base da equanimidade como fator da iluminação: dar
com freqüência atenção com sabedoria<span style="mso-spacerun: yes"> 
</span>para isso, esse é o alimento para o surgimento da equanimidade como um
fator da iluminação<i> </i>que ainda não surgiu ou para o crescimento e
incremento da equanimidade como fator da iluminação, uma vez que ela tenha
surgido.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Tal
