<p class=Normal><a name=N4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[4] As coisas que se tornam objeto da atenção plena: os quatro
fundamentos da atenção plena, os trinta e sete apoios para a iluminação e os
nove estados supramundanos. [<a href="#R4">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N5></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[5] As qualidades mentais escuras têm como contrapartida resultados
sombrios e as qualidades mentais claras têm como contrapartida resultados
luminosos, veja o <a href="MN57.php">MN 57</a>. Um exemplo abrangente de
qualidades mentais benéficas e prejudiciais pode ser encontrado no <a
href="MN8.php">MN 8</a>. A explicação deste fator da iluminação sugere que apesar
da “investigação dos fenômenos” ser identificada sob o ponto de vista técnico
com a sabedoria, <i>pañña, </i>a função inicial de <i>pañña </i>como fator da
iluminação não é discernir as três características, (sofrimento, etc.), mas
simplesmente discriminar entre as qualidades mentais benéficas e prejudiciais
que se tornam aparentes com o aprofundamento da atenção plena. [<a href="#R5">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[6] O elemento do estímulo, <i>(arambhadhatu), </i>é a fase inicial da
energia, o elemento do empenho, <i>(nikkamadhatu), </i>é o estágio
intermediário da energia, o elemento do esforço, <i>(parakkamadhatu), </i>é a
energia com plena intensidade. [<a href="#R6">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N7></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[7] Acalmar o corpo corresponde à tranqüilização dos três agregados
mentais (sensação, percepção e formações); tranqüilizar a mente corresponde à
tranqüilização da consciência. Os comentários com frequência interpretam corpo
e mente à luz do Abhidhamma que descreve o contraste entre a mente ou
consciência, <i>(citta), </i>o fator principal na cognição, e o “corpo” de
fatores mentais, <i>(cetasikas), </i>que acompanham a mente e que realizam
funções cognitivas secundárias. Parece no entanto que neste sutta a menção ao
corpo possui um sentido literal significando o corpo físico como um
contribuinte ativo no aspecto qualitativo da experiência. [<a href="#R7">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N8></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[8] O sinal da tranqüilidade, <i>(samathanimitta), </i>é a própria
tranqüilidade bem como o seu objeto, (<i>patibhaganimitta, </i>ou o sinal de contrapartida);
o sinal de não dispersão, <i>(abyagganimitta), </i>é um
sinônimo. [<a href="#R8">Retorna</a>]<o:p></o:p></span></p>







