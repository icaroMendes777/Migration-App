<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus, se
um bhikkhu abandona a cobiça pelo elemento consciência, então, devido ao
abandono da cobiça o suporte é eliminado e não existe base para o
estabelecimento da consciência. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando
