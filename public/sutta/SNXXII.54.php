<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em Savathi.
“Bhikkhus, existem esses cinco meios de reprodução. Quais cinco? Reprodução pela
raiz, reprodução pelo caule, reprodução pela articulação, reprodução pelo corte
e reprodução pela semente como a quinta. E se esses meios de reprodução não
estiverem rompidos, não estiverem arruinados, não estiverem danificados pelo
vento e pelo sol, se estiverem férteis e bem plantados, mas não houver terra e
não houver água, eles poderão exibir crescimento, aumento e expansão?<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>E se esses
cinco meios de reprodução estiverem rompidos, estiverem arruinados, estiverem
danificados pelo vento e pelo sol, estiverem férteis e mal plantados, mas se
houver terra e se houver água, eles poderão exibir crescimento, aumento e
expansão?<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>E se esses
cinco meios de reprodução não estiverem rompidos, não estiverem arruinados, não
estiverem danificados pelo vento e pelo sol, estiverem férteis e bem plantados,
e se houver terra e se houver água, eles poderão exibir crescimento, aumento e
expansão?<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Tal qual o
elemento terra, bhikkhus, é como os quatro pontos para o estabelecimento da
consciência devem ser vistos. Tal qual o elemento<span style="mso-spacerun:
yes">  </span>água é como o prazer e a cobiça devem ser vistos. Tal qual os
cinco meios de reprodução é como a consciência junto com o seu alimento deve
ser vista.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“A
consciência, bhikkhus, enquanto se mantém, poderá ser mantida apegada à forma,
suportada pela forma, fundamentada na forma e com um pouco de prazer, ela
poderá exibir crescimento, incremento e expansão.Ou, a consciência enquanto se
mantém poderá ser mantida apegada à sensação ... apegada à percepção ... apegada
às formações volitivas, suportada pelas formações volitivas, fundamentada nas formações volitivas, e com um
pouco de prazer, ela poderá exibir crescimento, incremento e expansão.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
embora alguém possa dizer: ‘Separado da forma, separado da sensação, separado
da percepção, separado das formações volitivas, eu declararei a vinda e ida da
consciência, o seu falecimento e renascimento, o seu crescimento, incremento e
expansão’ – isso seria impossível.<o:p></o:p></span></p>



