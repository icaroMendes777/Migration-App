<p class=Normal> “Bhikkhus, há  quatro tipos de oradores do Dhamma. Quais quatro? </p>

<p class=Normal> (1) "Aqui, bhikkhus, um certo orador do Dhamma fala pouco e o seu discurso é inútil, e a sua assembléia não tem habilidade em distinguir aquilo que é significativo do que é inútil. Esse orador é considerado como um orador do Dhamma por essa assembléia. </p>

<p class=Normal> (2) "Outro orador do Dhamma fala pouco mas o seu discurso tem significado, e a sua assembléia tem habilidade em distinguir aquilo que é significativo do que é inútil. Esse orador é considerado como um orador do Dhamma por essa assembléia. </p> 

<p class=Normal> (3) "Outro orador do Dhamma fala muito mas o seu discurso é inútil, e a sua assembléia não tem habilidade em distinguir aquilo que é significativo do que é inútil. Esse orador é considerado como um orador do Dhamma por essa assembléia. </p>

<p class=Normal> (4) "Outro orador do Dhamma fala muito e o seu discurso tem significado, e a sua assembléia tem habilidade em distinguir aquilo que é significativo do que é inútil. Esse orador é considerado como um orador do Dhamma por essa assembléia. </p> 

<p class=Normal>"Esses, bhikkhus são os quatro tipos de oradores do Dhamma." </p>

