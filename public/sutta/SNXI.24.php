Abençoado disse:]<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
há dois tipos de tolos: um que não enxerga uma transgressão como uma
transgressão; e um que, quando um outro está confessando uma transgressão, não
o perdoa de acordo com o Dhamma. Esses são os dois tipos de tolos. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
há dois tipos de pessoas sábias: uma que enxerga uma transgressão como uma
transgressão; e uma que, quando um outro está confessando uma transgressão, o
perdoa de acordo com o Dhamma. Esses são os dois tipos de pessoas sábias.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Certa vez
