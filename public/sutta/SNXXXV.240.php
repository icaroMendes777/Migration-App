ouvir um som com o ouvido ... Ao cheirar um aroma com o nariz ... Ao saborear um
sabor com a língua ... Ao tocar um tangível com o corpo ... Ao conscientizar um objeto
mental<span style="mso-spacerun: yes">  </span>com a<span style="mso-spacerun:
