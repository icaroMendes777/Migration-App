‘Talvez eu tenha uma oportunidade através do olho ... do ouvido ... do nariz ... da
língua ... do corpo. Talvez eu tenha uma oportunidade através da mente.’
Portanto, bhikkhus, permaneçam com as portas dos meios dos sentidos bem
guardadas. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ao ver uma forma com o
