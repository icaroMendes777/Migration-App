<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O chacal
também viu o jabuti à distância procurando alimento e assim ele se aproximou do
jabuti e ficou ali esperando, pensando, ‘Assim que o jabuti esticar algum dos
seus quatro membros ou o pescoço, eu vou agarrá-lo nesse exato momento, vou
arrancá-lo e comê-lo.’ Mas como o jabuti não esticou nenhum dos membros ou o pescoço,
o chacal, sem ter a sua oportunidade, perdeu o interesse e partiu.<span
style="mso-spacerun: yes">  </span><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Da mesma forma, bhikkhus<a
