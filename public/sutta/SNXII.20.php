<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savathi.”Bhikkhus, eu ensinarei para vocês a origem dependente e os fenômenos
com origem dependente. Ouçam e prestem muita atenção àquilo que eu vou dizer.”
– “Sim, venerável senhor,” os bhikkhus responderam. O Abençoado disse o
seguinte::<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O que,
bhikkhus, é origem dependente? Do nascimento como condição surge o
envelhecimento e morte. Havendo ou não o surgimento de <a name=PVW>um Tathagata</a>
essa propriedade se mantém – essa regularidade do Dhamma, essa ordenação do
Dhamma, essa condicionalidade isto/aquilo. O Tathagata desperta de forma direta
para isso, penetra isso. Despertando de forma direta e penetrando isso, ele o
explica, ensina, proclama, estabelece, revela, analisa, elucida. E ele diz,
‘Vejam, bhikkhus, do nascimento como condição, o envelhecimento e morte
[surge].<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Do
ser/existir como condição, o nascimento…<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Do apego
como condição, o ser/existir…<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Do desejo
como condição, o apego…<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Da
sensação como condição, o desejo…<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Do contato
como condição, a sensação…<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Das seis
bases dos sentidos como condição, o contato…<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Da
mentalidade-materialidade (nome e forma) como condição, as seis bases dos sentidos…<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Da
consciência como condição, a mentalidade-materialidade (nome e forma)…<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Das
formações volitivas como condição, a consciência…<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Da
ignorância como condição, as formações volitivas. Havendo ou não o surgimento de um
Tathagata essa propriedade se mantém – essa regularidade do Dhamma, essa
ordenação do Dhamma, essa condicionalidade isto/aquilo. O Tathagata desperta de
forma direta para isso, penetra isso. Despertando de forma direta e penetrando
isso, ele o explica, ensina, proclama, estabelece, revela, analisa,
elucida.<span style="mso-spacerun: yes">  </span>E ele diz, ‘Vejam, bhikkhus,
da ignorância como condição, as formações volitivas.’ <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
bhikkhus, a realidade disso, a infalibilidade, o que não é de outra forma, a
condicionalidade isto/aquilo: a isto se chama origem dependente.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que,
bhikkhus, são fenômenos com origem dependente? Envelhecimento e morte são
fenômenos com origem dependente: impermanentes, condicionados, com origem
dependente, sujeitos à destruição, ao desaparecimento, à cessação.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“
Nascimento é um fenômeno com origem dependente…<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“
Ser/existir é um fenômeno com origem dependente… <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“ Apego é
um fenômeno com origem dependente…<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“ Desejo é
um fenômeno com origem dependente…<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“ Sensação
é um fenômeno com origem dependente…<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“ Contato é
um fenômeno com origem dependente…<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“ As seis
bases dos sentidos são um fenômeno com origem dependente…<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“
Mentalidade-materialidade (nome e forma) é um fenômeno com origem dependente…<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“
Consciência é um fenômeno com origem dependente…<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“ Formações volitivas
são um fenômeno com origem dependente…<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“
Ignorância é um fenômeno com origem dependente: impermanente, condicionado, com
origem dependente, sujeito à destruição, ao desaparecimento, à cessação. Esses
são chamados os fenômenos com origem dependente.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando um
nobre discípulo viu bem, com correta sabedoria, essa origem dependente e esses
fenômenos com origem dependente, da forma como eles na verdade são, não é
possível que ele regresse ao passado, pensando, ‘Eu existi no passado? Não
existi no passado? O que fui no passado? Como eu era no passado? Tendo sido
que, no que me tornei no passado? Existirei no futuro? Não existirei no futuro?
O que serei no futuro? Como serei no futuro? Tendo sido que, no que me tornarei
no futuro?’ ou que ele esteja no seu íntimo perplexo acerca do presente: ‘Eu
sou? Eu não sou? O que sou? Como sou? De onde veio este ser? Para onde irá?’
Isso não é possível. Por que isso? Porque um nobre discípulo viu bem, com
correta sabedoria, essa origem dependente e esses fenômenos com origem
dependente, da forma como eles na verdade são.<o:p></o:p></span></p>


