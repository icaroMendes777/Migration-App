<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] O Sakya Mahanama era primo do Buda e irmão dos veneráveis Anuruddha
e Ananda. Ele preferiu permanecer como chefe de família permitindo que
Anuruddha se tornasse um bhikkhu. [</span><a href="#R1"><span lang=PT-BR
style='mso-ansi-language:PT-BR'>Retorna</span></a><span lang=PT-BR
