<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[2] De acordo com MA, Mahanama havia realizado o fruto do ‘que retorna
uma vez’ fazia muito tempo. Esse fruto apenas enfraquece a raiva e a delusão
sem no entanto erradicá-las. MA diz que ele tinha a noção equivocada de que a
cobiça, raiva e delusão eram eliminadas ao alcançar o caminho do que retorna
uma vez. Assim, ao ver que elas ainda surgiam na sua mente, compreendeu que
elas não haviam sido abandonadas e questionou o Buda das razões para o seu
surgimento. Os Nobres discípulos podem se enganar quanto a quais contaminações são
abandonadas por qual caminho supramundano. [</span><a href="#R2"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>Retorna</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=N3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[3] Da discussão que segue sobre os perigos dos prazeres sensuais,
parece que o “estado” (<i>dhamma</i>) não abandonado por Mahanama era o desejo
sensual, que o mantinha preso à vida em família e ao gozo dos prazeres
sensuais.[</span><a href="#R3"><span lang=PT-BR style='mso-ansi-language:PT-BR'>Retorna</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=N4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[4] MA: O “êxtase e prazer que estão afastados dos prazeres sensuais” é o
êxtase e prazer que pertencem ao primeiro e segundo jhanas; os estados “ainda
mais plenos de paz do que isso” são os jhanas mais elevados. Por este trecho
parece que um discípulo pode até mesmo alcançar os frutos do segundo caminho supramundano
(que retorna uma vez) sem ter realizado os jhanas mundanos.[</span><a href="#R4"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>Retorna</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>]</span><span lang=PT-BR style='font-size:11.0pt;
mso-bidi-font-size:12.0pt;mso-ansi-language:PT-BR'><o:p></o:p></span></p>



<p class=Normal><a name=N5></a> [5] Os</span><span
lang=PT-BR style='mso-ansi-language:PT-BR'> Niganthas ou Jainistas, discípulos
do mestre Nigantha Nataputta (também conhecido como Mahavira), enfatizavam a
prática de austeridades para desgastar o kamma ruim acumulado. O propósito
deste trecho, de acordo com MA, é mostrar a escapatória, que não havia sido
mostrada antes junto com a satisfação e o perigo dos prazeres sensuais. O Buda
usa a prática ascética dos Jainistas para demonstrar que o seu ensinamento é um
“Caminho do Meio” entre os dois extremos, o da entrega aos prazeres sensuais e
o da mortificação. [</span><a href="#R5"><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Retorna</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=N6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[6] Os Jainistas tinham a idéia que qualquer coisa que a pessoa
experimentasse seria causado por kamma passado. Se fosse assim, diz o Buda, as
dores intensas às quais eles se submetiam como parte da disciplina ascética
teriam que estar enraizadas em ações inábeis cometidas em vidas passadas. [</span><a
href="#R6"><span lang=PT-BR style='mso-ansi-language:PT-BR'>Retorna</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



