<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em certa
ocasião, o Abençoado estava em Savatthi no Bosque de Jeta, no Parque de
Anathapindika. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, ao amanhecer, a
bhikkhuni Alavika se vestiu e tomando a tigela e o manto externo foi para
Savatthi esmolar alimentos. Depois de haver esmolado alimentos em Savatthi e de
haver retornado, após a refeição, ela se dirigiu ao bosque dos Homens Cegos em
busca de isolamento. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, Mara, o Senhor do
Mal, desejando despertar medo, trepidação e terror na bhikkhuni Alavika,
desejando que ela abandonasse a sua concentração, se aproximou e se dirigiu a
ela em versos:<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR style='mso-ansi-language:PT-BR'>“ Não
existe escapatória do mundo,<br>
então o que você fará em isolamento?<br>
Desfrute das delícias do prazer sensual:<br>
não tenha remorsos mais tarde!” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Foi então que a bhikkhuni
Alavika pensou: “Agora, quem será que recitou esses versos – um ser humano ou
um ser não humano?” Então lhe ocorreu: “Foi Mara, o Senhor do Mal, quem recitou
esses versos desejando despertar medo, trepidação e terror em mim, desejando
que eu abandone a minha concentração.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, a bhikkhuni Alavika,
tendo compreendido, “Este é Mara, o Senhor do Mal,” respondeu em versos:<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR style='mso-ansi-language:PT-BR'>“Existe
uma escapatória do mundo<br>
que eu de perto toquei com a sabedoria.<br>
Oh Senhor do Mal, congênere dos negligentes,<br>
você não conhece esse estado.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR style='mso-ansi-language:PT-BR'>Os prazeres sensuais são
como espadas;<br>
os agregados, o seu degoladouro.<br>
Aquilo que você chama de prazer sensual<br>
para mim se tornou o não-prazer.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, Mara, o Senhor do
Mal, compreendendo que, “A bhikkhuni Alavika sabe quem sou,” triste e desapontado,
desapareceu no mesmo instante. <o:p></o:p></span></p>


