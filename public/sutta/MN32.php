<p class=Tit3 align=center style='text-align:center'><b> Majjhima Nikaya 32 </b></Tit3>
<p class=Tit1 align=center style='text-align:center'>Mahagosinga Sutta </Tit1>
<p class=Tit1 align=center style='text-align:center'>O Grande Discurso em Gosinga </Tit1>

