PT-BR;mso-bidi-font-weight:bold'>[5] Maha Kassapa era o discípulo mais
destacado dentre aqueles que seguiam as práticas ascéticas. [<a href="#R5">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR;mso-bidi-font-weight:bold'>[6] Abhidhamma. Embora essa palavra não deva
se referir ao Pitaka com esse nome – obviamente, o produto do pensamento
Budista posterior aos Nikayas – ela pode muito bem indicar uma abordagem
sistemática e analítica à doutrina que serviu como o núcleo original para o
Abhidhamma Pitaka. Num cuidadoso estudo dos contextos nos quais a palavra
“Abhidhamma” ocorre nos Suttas, o estudioso de Pali, Fumimaro Watanabe,
concluiu que os próprios discípulos do Buda formaram a concepção do Abhidhamma como
uma análise filosófica visando definir, analisar e classificar os dhammas e
explorar as suas relações mútuas. </span><span style='mso-bidi-font-weight:
bold'>Veja o seu livro <i>‘Philosophy and its Development in the Nikayas and
Abhidhamma’,</i> pág. 34-36. </span><span lang=PT-BR
style='mso-ansi-language:PT-BR;mso-bidi-font-weight:bold'>[<a href="#R6">Retorna</a>]<o:p></o:p></span></p>


