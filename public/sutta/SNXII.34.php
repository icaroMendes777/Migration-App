<p class=Normal> Em Savatthi. "Bhikkhus, eu lhes ensinarei setenta e sete tipos de conhecimento. Ouçam e prestem muita atenção àquilo que eu vou dizer.” – “Sim, venerável senhor,” os bhikkhus responderam. O Abençoado disse o seguinte: </p>


<p class=Normal>"Bhikkhus, quais são os setenta e sete tipos de conhecimento? O conhecimento: 'envelhecimento e morte tem o nascimento como condição.' O conhecimento: 'quando não há nascimento não há envelhecimento e morte.' O conhecimento: 'no passado também, não havendo nascimento não havia envelhecimento e morte.' O conhecimento: 'no futuro também envelhecimento e morte terão o nascimento como condição.' O conhecimento: 'no futuro também se não houver nascimento não haverá envelhecimento e morte.' O conhecimento: 'o conhecimento da regularidade do Dhamma também está sujeito à destruição, desaparecimento, e cessação.'

"O conhecimento: 'nascimento tem ser/existir como condição'  ... O conhecimento: 'as formações volitivas têm a ignorância como condição.' O conhecimento: 'quando não há ignorância não há formações volitivas.' O conhecimento: 'no passado também, não havendo ignorância não havia formações volitivas.' O conhecimento: 'no futuro também formações volitivas terão a ignorância como condição.' O conhecimento: 'no futuro também se não houver ignorância não haverá formações volitivas.' O conhecimento: 'o conhecimento da regularidade do Dhamma também estará sujeito à destruição, desaparecimento, e cessação.'

"Esses, bhikkhus, são chamados os setenta e sete tipos de conhecimento."
</p>

