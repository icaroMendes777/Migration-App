<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Asita, o vidente, na sua meditação do meio dia,<br>
viu os devas do Grupo dos Trinta<br>
&nbsp;&nbsp;&nbsp; - exultantes, estáticos - <br>
vestidos com puro branco, honrando a<span style="mso-spacerun: yes"> 
</span>Indra,<br>
segurando estandartes, aplaudindo desordenadamente,<br>
e vendo os devas assim felizes e contentes,<br>
ao saudá-los, ele disse: </span><span lang=PT-BR style='mso-bidi-font-size:
10.0pt;mso-ansi-language:PT-BR'><o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Por que a comunidade dos devas<br>
&nbsp;&nbsp;&nbsp; está tão desordenadamente exaltada?<br>
Por que vocês estão segurando e acenando <br>
os estandartes?<br>
Mesmo após a guerra com os Asuras<br>
&nbsp;&nbsp;&nbsp; - quando a vitória foi dos devas,<br>
&nbsp;&nbsp;&nbsp; os Asuras derrotados - <br>
mesmo então não havia uma excitação como essa.<br>
Vendo qual maravilha<br>
os devas estão tão felizes?<br>
&nbsp;&nbsp;&nbsp; Vocês gritam,<br>
&nbsp;&nbsp;&nbsp; cantam,<br>
&nbsp;&nbsp;&nbsp; tocam música,<br>
&nbsp;&nbsp;&nbsp; batem palmas,<br>
&nbsp;&nbsp;&nbsp; dançam.<br>
Então eu pergunto a vocês que vivem no topo do<span style="mso-spacerun: yes"> 
</span>Monte Meru.<br>
Por favor, estimados senhores, dissipem a minha dúvida logo.” <o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“O Bodisatva, a jóia mais preciosa,<br>
&nbsp;&nbsp;&nbsp; inigualável,<br>
nasceu para o bem-estar e felicidade<br>
&nbsp;&nbsp;&nbsp; do mundo humano,<br>
em uma cidade na região dos<span style="mso-spacerun: yes">  </span>Sakyas,<br>
&nbsp;&nbsp;&nbsp; Lumbini.<br>
É por isso que estamos todos desordenadamente exaltados.<br>
Ele, o supremo de todos os seres,<br>
a pessoa suprema,<br>
um touro entre os homens, o primeiro entre todas as pessoas,<br>
colocará em movimento a Roda [do Dhamma],<br>
no bosque<span style="mso-spacerun: yes">  </span>com o nome dos videntes,<br>
como um leão forte, rugindo,<br>
o conquistador de bestas.” <o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Ouvindo essas palavras,<br>
Asita rapidamente descendeu [do paraíso]<br>
e se dirigiu à residência de Suddhodana.<br>
Lá, tomando um assento, ele disse aos Sakyas:<br>
&nbsp;&nbsp;&nbsp; “Onde está o príncipe?<br>
&nbsp;&nbsp;&nbsp; Eu, também, quero vê-lo.”<br>
Os Sakyas então mostraram<br>
para o vidente chamado Asita<br>
&nbsp;&nbsp;&nbsp; o seu filho, o príncipe,<br>
&nbsp;&nbsp;&nbsp; como ouro brilhante,<br>
polido pelo mais hábil ourives<br>
na boca da fornalha,<br>
resplandecendo em glória, imaculado na cor.<br>
Vendo o príncipe resplandecendo como uma chama,<br>
puro como um cometa <br>
cruzando os céus<br>
&nbsp;&nbsp;&nbsp; - o sol incandescente,<br>
&nbsp;&nbsp;&nbsp; livre das nuvens do outono - <br>
ele ficou exultante, pleno com êxtase abundante.<br>
Os devas no céu seguravam<br>
um guarda sol com muitos raios<br>
e mil circulos.<br>
Pequenas vassouras com cabos de ouro<br>
ondulavam para cima e para baixo,<br>
porém aqueles que seguravam as vassouras e o guarda sol<br>
&nbsp;&nbsp;&nbsp; não podiam ser vistos.<br>
O vidente com o cabelo emaranhado e sujo<br>
denominado Esplendor Negro,<br>
vendo o menino, como um ornamento de ouro<br>
sobre o cobertor de lã vermelho,<br>
um guarda sol branco sobre a sua cabeça,<br>
o recebeu, feliz e contente.<br>
E ao receber o touro dos<span style="mso-spacerun: yes">  </span>Sakyas,<br>
ansioso, o mestre dos mantras e sinais<br>
exclamou com a mente confiante:<br>
&nbsp;&nbsp;&nbsp; “Este é insuperável,<br>
&nbsp;&nbsp;&nbsp; o mais elevado da raça bípede.”<br>
Então, prevendo a sua iminente partida,<br>
ele, deprimido, verteu lágrimas.<br>
Vendo-o chorar,<br>
os Sakyas perguntaram:<br>
&nbsp;&nbsp;&nbsp; “Mas com certeza não haverá<br>
&nbsp;&nbsp;&nbsp; perigo para o príncipe?”<br>
Vendo a preocupação dos Sakyas<br>
ele respondeu, “Eu prevejo para o príncipe<br>
&nbsp;&nbsp;&nbsp; nenhum dano.<br>
Nem haverá para ele qualquer perigo.<br>
Ele não é inferior: tenham certeza.<br>
&nbsp;&nbsp;&nbsp; Este príncipe alcançará<br>
&nbsp;&nbsp;&nbsp; a perfeita iluminação.<br>
Ele, vendo a máxima pureza,<br>
colocará em movimento a Roda do Dhamma<br>
por compaixão pelo bem-estar de muitos.<br>
A sua vida santa se espalhará em muitas direções.<br>
&nbsp;&nbsp;&nbsp; Mas quanto a mim<br>
minha vida aqui não tem mais resíduo;<br>
minha morte ocorrerá antes.<br>
&nbsp;&nbsp;&nbsp; Eu não ouvirei<br>
o Dhamma deste aqui com a tarefa inigualável.<br>
Essa é a razão porque estou aflito,<br>
&nbsp;&nbsp;&nbsp; atormentado e triste.” </span></p>


<p class=Normal style='margin-left:.75in'>Ele, tendo proporcionado aos Sakyas<br>
êxtase em abundância,<br>
aquele que seguiu a vida santa<br>
deixou o aposento íntimo e,<br>
por compaixão pelo seu sobrinho,<br>
o encorajou com relação ao Dhamma<br>
daquele com a tarefa inigualável:<br>
”Quando você ouvir de outrem a palavra,<br>
&nbsp;&nbsp;&nbsp; ‘Desperto,’<br>
ou ‘Tendo realizado a perfeita iluminação,<br>
ele abre o caminho do Dhamma,’<br>
vá até lá e faça o pedido.<br>
&nbsp;&nbsp;&nbsp; Siga a vida santa<br>
&nbsp;&nbsp;&nbsp; sob a orientação daquele Abençoado.” </p>


<p class=Normal style='margin-left:.75in'>Orientado por aquele<br>
cuja mente estava determinada pelo seu benefício,<br>
&nbsp;&nbsp;&nbsp; Assim,<br>
vendo no futuro a máxima pureza,<br>
Nalaka, que havia acumulado muito mérito,<br>
aguardava com esperanças o Vitorioso,<br>
guardando os seus sentidos.<br>
Ao ouvir que o Vitorioso<br>
havia colocado em movimento a roda do Dhamma,<br>
&nbsp;&nbsp;&nbsp; ele foi, ele viu<br>
o touro entre os videntes. Confiante,<br>
ele perguntou ao sábio mais destacado<br>
acerca da sabedoria mais elevada,<br>
agora que a previsão de Asita<br>
havia se concretizado. </p>


<p class=Normal><i>Nalaka:</i> </p>


<p class=Normal style='margin-left:.75in'>Agora que sei<br>
serem as palavras de Asita verdadeiras,<br>
Eu lhe pergunto, Gotama,<br>
você que realizou<br>
o além de todas as coisas.<br>
Eu estou determinado pela vida santa;<br>
Eu estou ansioso por esmolar alimentos.<br>
Diga-me sábio, eu lhe pergunto,<br>
acerca da sabedoria mais elevada. </p>


<p class=Normal><i>O Buda:</i></p>


<p class=Normal style='margin-left:.75in'>Eu lhe explicarei<br>
a sabedoria difícil de realizar,<br>
&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp; difícil de agüentar.<br>
Venha agora, eu lhe direi.<br>
Seja decidido. Seja firme.<br>
Pratique com equanimidade,<br>
pois num vilarejo<br>
há elogio e abuso.<br>
Afaste qualquer defeito do coração.<br>
Proceda com calma e sem arrogância.<br>
Coisas boas e ruins surgirão<br>
igual a labaredas em uma floresta.<br>
As mulheres seduzem um sábio.<br>
&nbsp;&nbsp;&nbsp; Que elas não o seduzam.[<a href="#N1">1</a>]<br>
<a name=R1></a>Abstendo-se do ato sexual,<br>
abandonando vários prazeres sensuais,<br>
não se oponha, não se apegue,<br>
para com os seres que se movam ou fiquem quietos.<br>
&nbsp;&nbsp;&nbsp; ‘Como eu sou, assim são eles.<br>
&nbsp;&nbsp;&nbsp; Como eles são, assim sou eu.’<br>
Fazendo um paralelo<br>
&nbsp;&nbsp;&nbsp; com você mesmo,<br>
não mate nem faça com que outros matem.<br>
Abandone os desejos e a cobiça<br>
nos quais as pessoas comuns estão aprisionadas,<br>
&nbsp;&nbsp;&nbsp; pratique com visão,<br>
&nbsp;&nbsp;&nbsp; atravesse este inferno.<br>
Sem encher o estômago,<br>
moderado na alimentação,<br>
tendo poucos desejos,<br>
sem ser cobiçoso,<br>
sempre sem ânsias pelo desejo:<br>
&nbsp;&nbsp;&nbsp; aquele que não ansia,<br>
&nbsp;&nbsp;&nbsp; está desatado. </p>


<p class=Normal style='margin-left:.75in'>Tendo esmolado alimentos, o sábio<br>
deve então ir para a floresta,<br>
em pé ou sentado<br>
&nbsp;&nbsp;&nbsp; ao pé de uma árvore.<br>
Decidido pelo jhana,<br>
deve encontrar o deleite na floresta,<br>
deve praticar jhana ao pé de uma árvore,<br>
alcançando a própria satisfação.<br>
Então, no final da noite,<br>
ele deve ir para o vilarejo,<br>
&nbsp;&nbsp;&nbsp; sem se deliciar com um convite<br>
&nbsp;&nbsp;&nbsp; ou presente do vilarejo.<br>
Tendo ido ao vilarejo,<br>
o sábio não deve descuidado<br>
estar com as famílias.<br>
Dando fim à conversa fútil,<br>
ele não deve pronunciar nenhuma intriga.<br>
&nbsp;&nbsp;&nbsp; ‘Eu obtive algo,<br>
&nbsp;&nbsp;&nbsp; excelente.<br>
&nbsp;&nbsp;&nbsp; Eu não obtive nada,<br>
&nbsp;&nbsp;&nbsp; ótimo.’<br>
Agindo assim em ambas situações,<br>
ele regressa para aquela mesma árvore.<br>
Perambulando segurando a sua tigela<br>
&nbsp;&nbsp;&nbsp; - ele não é tolo,<br>
&nbsp;&nbsp;&nbsp; mas parece tolo - <br>
ele não deve desprezar uma oferenda inútil<br>
nem menosprezar o doador. </p>


<p class=Normal style='margin-left:.75in'>Elevadas e baixas são as práticas<br>
proclamadas pelo contemplativo.<br>
Elas não conduzem até a outra margem duas vezes.<br>
Esta Libertação não é experimentada apenas uma vez.[<a href="#N2">2</a>] </p>


<p class=Normal style='margin-left:.75in'><a name=R2></a>Naquele que não tem apego - <br>
o bhikkhu que cruzou a torrente,<br>
abandonando aquilo que é<br>
e o que não é a tarefa - <br>
&nbsp;&nbsp;&nbsp; não existe febre. </p>


<p class=Normal style='margin-left:.75in'>Eu lhe explicarei a<br>
sabedoria:&nbsp;seja como o fio da navalha.<br>
Pressione a língua contra o palato,<br>
&nbsp;&nbsp;&nbsp; contenha o seu estômago.<br>
Não fique com a mente preguiçosa,<br>
nem repleta de pensamentos.<br>
Comprometa-se com a purificação das máculas,<br>
&nbsp;&nbsp;&nbsp; independente,<br>
tendo a vida santa como objetivo.<br>
Treine em solidão<br>
&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp; e na tarefa do contemplativo,<br>
&nbsp;&nbsp;&nbsp; A solidão <br>
&nbsp;&nbsp;&nbsp; é chamada<br>
&nbsp;&nbsp;&nbsp; sabedoria.<br>
Sozinho, você verdadeiramente se delicia<br>
&nbsp;&nbsp;&nbsp; e brilha nas dez direções.</p>


<p class=Normal style='margin-left:.75in'>Ao ouvir a fama dos iluminados<br>
&nbsp;&nbsp;&nbsp; - aqueles que praticam jhana,<br>
&nbsp;&nbsp;&nbsp; abandonando os prazeres sensuais - <br>
meus discípulos devem fomentar<br>
&nbsp;&nbsp;&nbsp; ainda mais<br>
&nbsp;&nbsp;&nbsp; a convicção e a consciência. </p>


<p class=Normal style='margin-left:.75in'>Aprenda com as águas<br>
nas rachaduras e fissuras:<br>
nos pequenos canais elas fluem<br>
&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp; ruidosamente,<br>
&nbsp;&nbsp;&nbsp; nos grandes<br>
&nbsp;&nbsp;&nbsp; silenciosamente.<br>
Tudo que não está cheio<br>
&nbsp;&nbsp;&nbsp; é ruidoso.<br>
tudo que está cheio<br>
&nbsp;&nbsp;&nbsp; é silencioso.<br>
O tolo é igual a uma jarra meio vazia;<br>
aquele que é sábio, um lago cheio.<br>
Um contemplativo que fala muito<br>
&nbsp;&nbsp;&nbsp; munido com bom senso:<br>
&nbsp;&nbsp;&nbsp; sabendo, ele ensina o Dhamma,<br>
&nbsp;&nbsp;&nbsp; sabendo, ele fala muito.<br>
Mas aquele que,<br>
&nbsp;&nbsp;&nbsp; sabendo, é contido<br>
&nbsp;&nbsp;&nbsp; sabendo, não fala muito:<br>
ele é um sábio<br>
&nbsp;&nbsp;&nbsp; digno da sapiência;<br>
ele é um sábio,<br>
&nbsp;&nbsp;&nbsp; com a sapiência realizada. </p>


