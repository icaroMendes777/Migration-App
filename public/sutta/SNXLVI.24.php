<p class=Normal> “Bhikkhus, quando alguém dá atenção sem sabedoria, o desejo sensual que ainda não surgiu, surge, e o desejo sensual que já surgiu aumenta e expande. Quando alguém dá atenção sem sabedoria, a má vontade que ainda não surgiu, surge, e a má vontade que já surgiu aumenta e expande. Quando alguém dá atenção sem sabedoria, a preguiça e torpor que ainda não surgiram, surgem, e a preguiça e torpor que já surgiram aumentam e expandem. Quando alguém dá atenção sem sabedoria, a inquietação e ansiedade que ainda não surgiu, surgem, e a inquietação e ansiedade que já surgiram aumentam e expandem. Quando alguém dá atenção sem sabedoria, a dúvida que ainda não surgiu, surge, e a dúvida que já surgiu aumenta e expande. Além disso, o fator da iluminação da atenção plena que ainda não surgiu, não surge, e o fator da iluminação da atenção plena que já surgiu cessa ... o fator da iluminação da equanimidade que ainda não surgiu, não surge, e o fator da iluminação da equanimidade que já surgiu cessa.</p>
 
<p class=Normal> “Quando alguém dá atenção com sabedoria, o desejo sensual que ainda não surgiu, não surge, e o desejo sensual que já surgiu é abandonado. Quando alguém dá atenção com sabedoria, a má vontade que ainda não surgiu ... preguiça e torpor ... inquietação e ansiedade ... dúvida que ainda não surgiu, não surge, e a dúvida que já surgiu é abandonada. Além disso, o fator da iluminação da atenção plena que ainda não surgiu, surge, e o fator da iluminação da atenção plena que já surgiu é consumado através do desenvolvimento ... o fator da iluminação da equanimidade que ainda não surgiu, surge, e o fator da iluminação da equanimidade que já surgiu é consumado através do desenvolvimento."</p>

