Dhamma. Assim foi como o venerável Vidhura acabou sendo chamado ‘Vidhura.’ [<a
href="#N3">3</a>] <a name=R3></a>Mas o venerável Sanjiva, ia para a floresta, 
ou para a sombra de uma árvore, ou para um local isolado e entrava sem dificuldade na cessação da
percepção e sensação. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. “Certa
