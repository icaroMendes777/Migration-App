Moggallana e penetrou nos seus intestinos. Então o venerável Maha Moggallana
pensou o seguinte: “Porque a minha barriga está tão pesada? Poderia se imaginar
que está cheia de feijão.” Então, ele parou de caminhar e foi para a sua
moradia onde ele se sentou num assento que estava preparado.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. Depois
de ter sentado, ele deu minuciosa atenção a si mesmo e ele viu que Mara, o
Senhor do Mal, havia entrado na sua barriga e penetrado nos seus intestinos. Ao
ver isso, ele disse: “Saia daí, Senhor do Mal! Saia daí, Senhor do Mal! Não
incomode o Tathagata, não incomode os discípulos do Tathagata ou isso lhe trará
dano e sofrimento por muito tempo.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. Então
Mara, o Senhor do Mal pensou: “Esse contemplativo não me conhece, ele não me vê
quando diz isso. Até mesmo o mestre dele não me conheceria tão rápido, então
como pode esse discípulo me conhecer?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. Então o
venerável Maha Moggallana disse: “Ainda assim eu o conheço, Senhor do Mal. Não
pense: ‘Ele não me conhece.’ Você é Mara, Senhor do Mal. Você estava pensando
assim, Senhor do Mal: ‘Esse contemplativo não me conhece, ele não me vê quando
diz isso. Até mesmo o mestre dele não me conheceria tão rápido, então como pode
esse discípulo me conhecer?’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. Então
Mara, o Senhor do Mal pensou: “O contemplativo me conheceu, ele me viu quando
disse aquilo,” saindo em seguida pela boca do venerável Maha Moggallana, ficando
em pé contra a tranca da porta. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. O
venerável Maha Moggallana viu Mara ali em pé e disse: “Eu também o vejo aí,
Senhor do Mal. Não pense: ‘Ele não me vê.’ Você está em pé contra a tranca da
porta, Senhor do Mal. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. “Certa
vez aconteceu, Senhor do Mal, que eu era um Mara chamado Dusi,[<a href="#N1">1</a>]
<a name=R1></a>e eu tinha uma irmã chamada Kali. Você era o filho dela,
portanto você era o meu sobrinho.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. “Agora,
naquela ocasião o Abençoado Kakusandha, um arahant, perfeitamente iluminado, havia
surgido no mundo. [<a href="#N2">2</a>] <a name=R2></a>O Abençoado Kakusandha,
um arahant, perfeitamente iluminado, tinha um auspicioso par de discípulos
