seu manto e depois se vestiu e tomando a tigela e o manto externo foi para o
vilarejo esmolar alimentos. Os vaqueiros, pastores e lavradores que por ali
passavam viram o venerável Sanjiva esmolando alimentos e eles pensaram: ‘É
maravilhoso, senhores, é admirável! Esse contemplativo que estava lá sentado,
morto, ressuscitou!’ Assim foi como o venerável Sanjiva acabou sendo chamado
‘Sanjiva.’ [<a href="#N5">5</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R5></a><span lang=PT-BR style='mso-ansi-language:
