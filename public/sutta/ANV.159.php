até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse: <o:p></o:p></span></p>


<p class=Normal>&quot;O Venerável Udayin, senhor, está sentado cercado por
uma grande assembléia de chefes de família, ensinando o<span
style="mso-spacerun: yes">  </span>Dhamma.&quot; <o:p></o:p></span></p>


<p class=Normal>&quot;Não é fácil ensinar o Dhamma, Ananda. O Dhamma deveria
ser ensinado somente quando cinco qualidades estiverem estabelecidas
na pessoa que ensina. Quais cinco? <o:p></o:p></span></p>


<p class=Normal>&quot;[1] O Dhamma deve ser ensinado com o pensamento, ‘Eu falarei
de modo gradual.' [<a href="#N1">1</a>]<o:p></o:p></span></p>


<p class=Normal>&quot;[2] O Dhamma deve ser ensinado com o pensamento, ‘Eu falarei
explicando a seqüência [de causa e efeito].' <o:p></o:p></span></p>

<p class=Normal>&quot;[3] O Dhamma deve ser ensinado com o pensamento, ‘Eu falarei por
compaixão.’ <o:p></o:p></span></p>


<p class=Normal>&quot;[4] O Dhamma deve ser ensinado com o pensamento, ‘Eu falarei
sem o propósito de recompensa material.’<o:p></o:p></span></p>


<p class=Normal>&quot;[5] O Dhamma deve ser ensinado com o pensamento, ‘Eu falarei sem
lesar a mim ou os outros.’ [<a href="#N2">2</a>] <o:p></o:p></span></p>


<p class=Normal>&quot;Não é fácil ensinar o Dhamma, Ananda. O Dhamma deve ser
ensinado  somente quando essas cinco qualidades estiverem estabelecidas
na pessoa que ensina. <o:p></o:p></span></p>
