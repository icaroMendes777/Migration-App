<p class=Normal>Ouvi que em certa ocasião o Abençoado estava em Kosambi, no Parque de
Ghosita. Nessa ocasião o Venerável Udayin estava sentado cercado por uma grande assembléia
de chefes de família, ensinando o<span style="mso-spacerun: yes"> 
</span>Dhamma. O Venerável Ananda viu o<span style="mso-spacerun: yes">  </span>Venerável
Udayin sentado cercado por uma grande assembléia de chefes de família,
