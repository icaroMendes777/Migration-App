<p class=Normal> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: "Bhikkhus, aspirando três tipos de felicidade, uma pessoa sábia deveria  observar a conduta virtuosa. Quais são os três? Aspirando 'Que eu seja elogiado,' uma pessoa sábia deveria  observar a conduta virtuosa. Aspirando 'Que eu obtenha riqueza,' uma pessoa sábia deveria  observar a conduta virtuosa. Aspirando 'Com a dissolução do corpo, após a morte, que eu renasça num destino feliz, no paraíso,' uma pessoa sábia deveria  observar a conduta virtuosa. Esses são os três tipos de felicidade pelos quais aspirando uma pessoa sábia deveria  observar a conduta virtuosa."</p>

<p class=Normal style='margin-left:.75in'> Aspirando por três tipos de felicidade<br>
uma pessoa sábia deveria  observar a conduta virtuosa: <br>
elogios, riqueza, <br>
deliciar-se no paraíso mais além. </p>

<p class=Normal style='margin-left:.75in'> Se uma pessoa não comete o mal<br>
se associa com quem comete o mal, <br>
ela será suspeita do mal<br>
e a sua má reputação aumenta.</p>

<p class=Normal style='margin-left:.75in'>Qualquer amizade que ela tenha, <br>
com quem quer que se associe, <br>
a pessoa passará a ter o mesmo caráter, <br>
ela será igual ao companheiro. </p>

<p class=Normal style='margin-left:.75in'>O seguidor e o seguido, <br>
aquele que faz o contato e o contatado, <br>
são como uma flecha besuntada com veneno<br>
que contamina a aljava. <br>
Temendo a contaminação, a pessoa sábia<br>
não deve ter amizades ruins. </p>

<p class=Normal style='margin-left:.75in'>Alguém que embale com folhas <br>
 um peixe podre<br>
fará com que as folhas tenham o mesmo odor; <br>
o mesmo ocorre com aqueles que vão atrás dos tolos. </p>

<p class=Normal style='margin-left:.75in'>Mas alguém que espalhe um pó perfumado<br>
numa folha larga de uma árvore<br>
fará com que a folha fique perfumada; <br>
o mesmo ocorre com aqueles que vão atrás dos sábios. </p>

<p class=Normal style='margin-left:.75in'>Portanto, tal como a folha larga, <br>
compreendendo os resultados para si mesmo, <br>
não siga os não virtuosos. <br>
Uma pessoa sábia segue os virtuosos. <br>
Os não virtuosos conduzem ao inferno, <br>
os virtuosos ajudam chegar no paraíso. </p>


