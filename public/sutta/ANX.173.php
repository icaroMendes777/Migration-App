<p class=Normal> "Bhikkhus, aquilo que é não-Dhamma e aquilo que é Dhamma deve ser compreendido, e aquilo que é prejudicial e aquilo que é benéfico deve ser compreendido. Tendo compreendido aquilo que é não-Dhamma e aquilo que é Dhamma, e também aquilo que é prejudicial e aquilo que é benéfico, deve-se praticar de acordo com o Dhamma e com o que é benéfico. </p>

<p class=Normal>"Bhikkhus, o que é não-Dhamma, e o que é Dhamma? O que é prejudicial e o que é benéfico? </p>

<p class=Normal> (1) "Matar seres vivos é não-Dhamma; abster-se de matar seres vivos é Dhamma. As inúmeras qualidades ruins e prejudiciais que se originam de matar seres vivos como condição: essas são prejudiciais. As inúmeras qualidades benéficas que se realizam através do desenvolvimento tendo a abstenção de matar seres vivos como condição: essas são benéficas. </p>

<p class=Normal> (2) "Tomar aquilo que não for dado é não-Dhamma; abster-se de tomar aquilo que não for dado é Dhamma ... (3) A conduta sexual imprópria é não-Dhamma; abster-se da conduta sexual imprópria é Dhamma ... (4) A linguagem mentirosa é não-Dhamma; abster-se da linguagem mentirosa é Dhamma ... (5) A linguagem maliciosa é não-Dhamma; abster-se da linguagem maliciosa é Dhamma ... (6) A linguagem grosseira é não-Dhamma; abster-se da linguagem grosseira é Dhamma ... (7) A linguagem frívola é não-Dhamma; abster-se da linguagem frívola é Dhamma ... (8) A cobiça é não-Dhamma; abster-se da cobiça é Dhamma ... (9) A má vontade é não-Dhamma; boa vontade é Dhamma ... </p>

<p class=Normal> (10) "Entendimento incorreto é não-Dhamma; entendimento correto é Dhamma. As inúmeras qualidades ruins e prejudiciais que se originam do entendimento incorreto como condição: essas são prejudiciais. As inúmeras qualidades benéficas que se realizam através do desenvolvimento tendo o entendimento correto como condição: essas são benéficas. </p>

<p class=Normal>"Quando foi dito: 'Bhikkhus, aquilo que é não-Dhamma e aquilo que é Dhamma deve ser compreendido, e aquilo que é prejudicial e aquilo que é benéfico deve ser compreendido. Tendo compreendido aquilo que é não-Dhamma e aquilo que é Dhamma, e também aquilo que é prejudicial e aquilo que é benéfico, deve-se praticar de acordo com o Dhamma e com o que é benéfico,' é por isso que isso foi dito." </p>


