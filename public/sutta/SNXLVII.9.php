<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim ouvi.
[</span><a href="#N1"><span lang=PT-BR style='mso-ansi-language:PT-BR'>1</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R1></a>Em certa ocasião o
Abençoado estava<span style='mso-spacerun:yes'>  </span>em Vesali em
Beluvagamaka. Lá ele se dirigiu aos monges desta forma:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venham,
bhikkhus, entrem no retiro das chuvas onde quer que vocês tenham amigos,
conhecidos e familiares nas proximidades de Vesali. Eu mesmo irei entrar no
retiro das chuvas aqui em Beluvagamaka.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
venerável senhor,” aqueles bhikkhus responderam e eles entraram no retiro das
chuvas onde tinham amigos, conhecidos e familiares nas proximidades de Vesali,
enquanto que o Abençoado entrou no retiro das chuvas ali mesmo em Beluvagamaka.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então,
quando o Abençoado havia entrado no retiro das chuvas, ele foi acometido por
uma grave enfermidade e dores terríveis próximas da morte o assaltaram. Mas o
Abençoado suportou aquilo, com atenção plena e plena consciência, sem ficar
aflito. Então este pensamento ocorreu ao Abençoado: “Não é apropriado que eu
realize o parinibbana sem ter falado com os meus assistentes e sem ter me
despedido da Sangha dos Bhikkhus. Que eu então suprima esta enfermidade através
da energia e siga vivendo, tendo decidido pela formação vital.[<a href="#N2">2</a>]
<a name=R2></a>Então, o Abençoado suprimiu aquela enfermidade através da
energia e seguiu vivendo, tendo decidido pela formação vital.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O Abençoado
então se recuperou daquela enfermidade. Pouco tempo depois dele haver se
recuperado, ele saiu da sua moradia e sentou num assento que havia sido
preparado à sombra atrás da habitação. Então, o Venerável Ananda foi até o
Abençoado e depois de cumprimentá-lo sentou a um lado e disse: “É esplêndido,
venerável senhor que o Abençoado esteja resistindo bem, é esplêndido que ele
tenha se recuperado! Mas, venerável senhor, quando o Abençoado estava enfermo
foi como se o meu corpo estivesse sedado, eu me senti perdido, as coisas não
estavam claras para mim. Todavia, eu tinha este tanto de consolo: que o
Abençoado não iria realizar o parinibbana sem ter feito algum pronunciamento
relacionado à Sangha dos Bhikkhus.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O que
mais, Ananda, espera de mim a Sangha dos Bhikkhus? Eu ensinei o Dhamma sem
fazer qualquer distinção entre ensinamentos esotéricos e exotéricos, não há
nada, Ananda, concernente aos ensinamentos que o Tathagata tenha retido até o
final com o punho cerrado de um mestre que não transmite todos os ensinamentos.
Se, Ananda, alguém pensar, ‘Eu assumirei o controle<span
style='mso-spacerun:yes'>  </span>da Sangha dos Bhikkhus,’ ou ‘A Sangha dos
bhikkhus está sob o meu comando,’ é ele que deveria fazer um pronunciamento desses
para a Sangha dos Bhikkhus. Mas, o Tathagata não pensa assim, ‘Eu assumirei o
controle<span style='mso-spacerun:yes'>  </span>da Sangha dos Bhikkhus,’ ou ‘A
Sangha dos bhikkhus está sob o meu comando,’ então porque deveria o Tathagata
fazer algum pronunciamento desses para a Sangha dos Bhikkhus? Eu agora estou
velho, Ananda, com a idade avançada, atribulado pelos anos, avançado na vida, já no
último estágio. Este é o meu octogésimo ano. Assim como uma
carroça velha é mantida por meio de ataduras, da mesma forma o corpo do
Tathagata é mantido por meio de uma combinação de ataduras. Sempre que, Ananda,
através da não atenção a todos os sinais e pela cessação de certas sensações, o
Tathagata entra e permanece na concentração sem sinais da mente, nessa ocasião,
Ananda, o corpo do Tathagata fica mais confortável.<span
style='mso-spacerun:yes'>  </span><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
Ananda, sejam ilhas para vocês mesmos, refúgios para vocês mesmos, buscando
nenhum refúgio externo; com o Dhamma como a sua ilha, o Dhamma como o seu
refúgio, buscando nenhum outro refúgio. E como, Ananda, um bhikkhu é uma ilha
para ele mesmo, um refúgio para ele mesmo, buscando nenhum refúgio externo; com
o Dhamma como a sua ilha, o Dhamma como o seu refúgio, buscando nenhum outro
refúgio? Neste caso, Ananda, um bhikkhu permanece contemplando o corpo como um
corpo, ardente, plenamente consciente e com atenção plena, tendo colocado de
lado a cobiça e o desprazer pelo mundo. Ele permanece contemplando as sensações
como sensações ... mente como mente ... objetos mentais como objetos mentais,
ardente, plenamente consciente e com atenção plena, tendo colocado de lado a
cobiça e o desprazer pelo mundo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Esses
bhikkhus,<span style='mso-spacerun:yes'>  </span>Ananda, que agora ou depois
que eu me for, permanecerem como uma ilha para eles mesmos, como um refúgio
para eles mesmos, sem nada mais como refúgio, com o Dhamma como uma ilha, o
Dhamma como refúgio, sem nada mais como refúgio, eles serão para mim os
bhikkhus mais eminentes dentre aqueles que têm interesse em aprender.”<o:p></o:p></span></p>

