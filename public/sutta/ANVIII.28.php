<p class=Normal> Então o venerável Sariputta foi até o Abençoado e depois de cumprimentá-lo sentou a um lado. O Abençoado disse o seguinte: </p>

<p class=Normal> "Sariputta, quando as impurezas de um bhikkhu foram destruídas, ele possui oito poderes com base nos quais ele pode reivindicar: 'Minhas impurezas foram destruídas.' Quais oito?" </p>

<p class=Normal> "Venerável senhor, quando as impurezas de um bhikkhu foram destruídas, ele possui oito poderes com base nos quais ele pode reivindicar: 'Minhas impurezas foram destruídas.' Quais oito? </p>

<p class=Normal> (1) "Aqui, venerável senhor, um bhikkhu com as impurezas destruídas viu com clareza todos os fenômenos condicionados como eles na verdade são, com correta sabedoria, como impermanentes. Esse é um poder de um bhikkhu com as impurezas destruídas com base no qual ele pode reivindicar: 'Minhas impurezas foram destruídas.'</p>

<p class=Normal> (2) 'Outra vez, um bhikkhu com as impurezas destruídas viu com clareza os prazeres dos sentidos como eles na verdade são, com correta sabedoria, como uma cova com carvão em brasas. Esse é um poder de um bhikkhu com as impurezas destruídas com base no qual ele pode reivindicar: 'Minhas impurezas foram destruídas.'</p>

<p class=Normal> (3) 'Outra vez, um bhikkhu com as impurezas destruídas decliva, tende e se inclina para o isolamento; ele está afastado, deliciando-se com a renúncia, e deu um fim completo a todas as coisas que são a base para as impurezas. Esse é um poder de um bhikkhu com as impurezas destruídas com base no qual ele pode reivindicar: 'Minhas impurezas foram destruídas.'</p>

<p class=Normal> (4) "Outra vez, um bhikkhu com as impurezas destruídas desenvolveu, bem desenvolvidos, os quatro fundamentos da atenção plena. Esse é um poder de um bhikkhu com as impurezas destruídas com base no qual ele pode reivindicar: 'Minhas impurezas foram destruídas.'</p>

<p class=Normal> (5)-(8) "Outra vez, um bhikkhu com as impurezas destruídas desenvolveu, bem desenvolvidas (os), as quatro bases do poder espiritual ... as cinco faculdades espirituais ... os sete fatores da iluminação ... o nobre caminho óctuplo. Esse é um poder de um bhikkhu com as impurezas destruídas com base no qual ele pode reivindicar: 'Minhas impurezas foram destruídas.'</p>

<p class=Normal> "Venerável senhor, quando as impurezas de um bhikkhu foram destruídas, ele possui esses oito poderes com base nos quais ele pode reivindicar: 'Minhas impurezas foram destruídas.'"
</p>

