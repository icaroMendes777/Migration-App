<p class=Normal>"Bhikkhus, há essas quatro coisas que devem ser realizadas. Quais quatro? </p>

<p class=Normal>"Há coisas que devem ser realizadas com o corpo, que devem ser realizadas com a memória, que devem ser realizadas com o olho, que devem ser realizadas com a sabedoria. </p>

<p class=Normal>"O que deve ser realizado com o corpo? As oito libertações. </p>

<p class=Normal>"O que deve ser realizado com a memória? Os nascimentos passados. </p>

<p class=Normal> "O que deve ser realizado com o olho? A morte e renascimento dos seres. </p>

<p class=Normal>"O que deve ser realizado com a sabedoria? A destruição das impurezas. </p>

<p class=Normal>"Essas, bhikkhus são as quatro coisas que devem ser realizadas." </p>

