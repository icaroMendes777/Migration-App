<p class=Normal><b>Notas:</b></p>

<p class=Normal>Ajaan Sujato comenta o seguinte em relação a este sutta: </p>

<p class=Normal>"Este sutta é um bom exemplo do uso de linguagem metafórica no ensino do Dhamma. Em particular o uso dos termos 'corpo' e 'olho'. É praticamente impossível que 'olho' neste caso signifique o olho físico; ninguém argumentaria ser possível ver seres renascendo através do olho físico. Neste contexto o olho é uma metáfora para a refinada visão interior. </p>

<p class=Normal>Da mesma forma, o corpo não é o corpo físico mas uma metáfora para a experiência direta e completa. Se isso não parece óbvio do contexto, observe que as coisas para serem realizadas com o corpo são as oito libertações que incluem as quatro realizações imateriais que por definição estão além de qualquer realidade física. Em outros suttas o Buda diz que até mesmo nibbana deve ser realizado com o corpo." </p>

<p class=Normal>Ajaan Sujato traduz <i>satiya</i> como "atenção plena" ao invés de "memória" como foi traduzido aqui. </p>

