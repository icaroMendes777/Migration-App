<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] Dandapani, cujo nome significa “bengala na mão,” era assim chamado
porque ele costumava andar de forma ostensiva com uma bengala de ouro, apesar
de ser ainda jovem e saudável. De acordo com MA, ele fazia parte do grupo de
Devadatta, o arquiinimigo do Buda, quando este tentou criar um cisma entre os
discípulos do Buda. A maneira pela qual ele formula a pergunta é arrogante,
provocativa e proposital. [</span><a href="#R1"><span lang=PT-BR
style='mso-ansi-language:PT-BR'>Retorna</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[2] A primeira parte é uma resposta direta do Buda à atitude agressiva
de Dandapani. MA menciona o <a href="SNXXII.94.php">SN XXII.94</a>:
&quot;Bhikkhus, eu não disputo com o mundo, é o mundo que disputa comigo. Quem
fala o Dhamma não disputa com ninguém no mundo.&quot;<span
style='mso-spacerun:yes'>  </span>A segunda parte pode ser interpretada como
significando que para o arahant (mencionado como 'aquele brâmane', referindo-se
ao próprio Buda), as percepções não mais despertam as tendências subjacentes enumeradas no verso 8. [</span><a href="#R2"><span lang=PT-BR
style='mso-ansi-language:PT-BR'>Retorna</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=N3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[3] Essa resposta parece ser uma expressão de frustração e confusão. [</span><a
href="#R3"><span lang=PT-BR style='mso-ansi-language:PT-BR'>Retorna</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=N4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[4] A interpretação desse trecho obscuro depende da palavra <i
style='mso-bidi-font-style:normal'>papanca</i> e do composto <i
style='mso-bidi-font-style:normal'>papanca-sañña-sankha.<br> </i>Em um estudo
profundo <i style='mso-bidi-font-style:normal'>(Concept and Reality in Early
Buddhism)</i> o Bhikkhu Ñanananda explica <i style='mso-bidi-font-style:normal'>papanca</i>
como &quot;proliferação conceitual&quot; ou seja a propensão da imaginação da
pessoa mundana de irromper em uma profusão de comentários mentais que
obscurecem o reconhecimento dos fenômenos isentos de qualificação.<br> Os
comentários indicam a fonte dessa proliferação como sendo os três fatores -
desejo, presunção e idéias - por conta dos quais a mente &quot;embeleza&quot; a
experiência interpretando-a em termos de &quot;meu,&quot; &quot;eu&quot; e
&quot;pertencente ao meu eu.&quot; <i>Papanca </i>é portanto semelhante a <i>maññana,
</i>“conceber”, encontrado no <a href="MN1.php#N6">MN1 – nota 6</a>.<br> 

O composto
<i>papanca-sañña-sankha</i> é um pouco mais problemático. O ven.
Ñanananda interpreta o significado como sendo &quot;conceitos, suposições, designações ou convenções linguísticas caracterizadas
pela tendência prolífica da mente,&quot; mas essa interpretação não toma em
conta a palavra <i style='mso-bidi-font-style:normal'>sañña.<br> </i>MA explica <i
style='mso-bidi-font-style:normal'>sankha</i> com <i style='mso-bidi-font-style:
normal'>kotthasa, </i>&quot;porção,&quot; e diz que <i style='mso-bidi-font-style:
normal'>sañña</i> ou é a percepção associada com <i style='mso-bidi-font-style:
normal'>papanca</i> ou <i style='mso-bidi-font-style:normal'>papanca</i> mesmo.
Esta tradução segue a interpretação do ven. Ñanananda ao interpretar <i
style='mso-bidi-font-style:normal'>sankha</i> como sendo concepção ou noção ao
invés de porção.<br> A decisão de tratar<i style='mso-bidi-font-style:normal'>
sañña-sankha </i><span style='mso-spacerun:yes'> </span>como o composto
&quot;percepções e concepções&quot; pode ser questionada, mas a expressão <i
style='mso-bidi-font-style:normal'>papanca-sañña-sankha</i> ocorre apenas
raramente no Cânone e nunca é analisada verbalmente de forma que nenhuma
interpretação está isenta de questionamento.<br> Em interpretações alternativas dos
seus componentes, a expressão poderia ser formulada como &quot;concepções (que
surgem da) proliferação das percepções&quot; ou &quot;concepções perceptivas
(que surgem da) proliferação.”<br> Na seqüência ficará claro que o próprio processo
de cognição é &quot;a fonte através da qual as percepções e concepções
impregnadas pela proliferação mental atormentam um homem.&quot; Se não há nada
no processo cognitivo que deleite, que seja bem vindo, que deva ser mantido, as
tendências subjacentes serão eliminadas. [<a href="#R4">Retorna</a>]<o:p></o:p></span></p>



