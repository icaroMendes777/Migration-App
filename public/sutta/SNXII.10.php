<p class=Normal>Em Savatthi. </p>

<p class=Normal> <b>(Originação)</b></p>

<p class=Normal>"Bhikkhus, antes da minha iluminação, quando eu ainda era apenas um Bodisatva não iluminado, eu pensei: 'Ah! Este mundo enfrenta dificuldades visto que nasce, envelhece, morre, falece e renasce, no entanto, não compreende a escapatória desse sofrimento [encabeçado] pelo envelhecimento e morte. Quando afinal será discernida a escapatória desse sofrimento [encabeçado] pelo envelhecimento e morte?'</p>

<p class=Normal>"Então, bhikkhus eu pensei: 'Quando existe o quê surge o envelhecimento e morte? Qual a condição para o envelhecimento e morte?' Então, bhikkhus, empregando a atenção com sabedoria ocorreu a realização através da sabedoria: 'Quando há o nascimento, o envelhecimento e morte surgem; o envelhecimento e morte têm o nascimento como condição.'</p>

<p class=Normal>"Então, bhikkhus eu pensei: 'Quando existe o quê surge o nascimento? Qual a condição para o nascimento?' Então, bhikkhus, empregando a atenção com sabedoria ocorreu a realização através da sabedoria: 'Quando há o ser/existir, o nascimento surge; o nascimento tem o ser/existir como condição.'</p>

<p class=Normal>"Então, bhikkhus eu pensei: 'Quando existe o quê surge o ser/existir? Qual a condição para o ser/existir?' Então, bhikkhus, empregando a atenção com sabedoria ocorreu a realização através da sabedoria: 'Quando há o apego, o ser/existir surge; o ser/existir tem o apego como condição.'</p>

<p class=Normal>"Então, bhikkhus eu pensei: 'Quando existe o quê surge o apego? Qual a condição para o apego?' Então, bhikkhus, empregando a atenção com sabedoria ocorreu a realização através da sabedoria: 'Quando há o desejo, o apego surge; o apego tem o desejo como condição.'</p>

<p class=Normal>"Então, bhikkhus eu pensei: 'Quando existe o quê surge o desejo? Qual a condição para o desejo?' Então, bhikkhus, empregando a atenção com sabedoria ocorreu a realização através da sabedoria: 'Quando há a sensação, o desejo surge; o desejo tem a sensação como condição.'

<p class=Normal>"Então, bhikkhus eu pensei: 'Quando existe o quê surge a sensação? Qual a condição para a sensação?' Então, bhikkhus, empregando a atenção com sabedoria ocorreu a realização através da sabedoria: 'Quando há o contato, a sensação surge; a sensação tem o contato como condição.'</p>

<p class=Normal>"Então, bhikkhus eu pensei: 'Quando existe o quê surge o contato? Qual a condição para o contato?' Então, bhikkhus, empregando a atenção com sabedoria ocorreu a realização através da sabedoria: 'Quando há as seis bases dos sentidos, o contato surge; o contato tem as seis bases dos sentidos como condição.'</p>

<p class=Normal>"Então, bhikkhus eu pensei: 'Quando existe o quê surgem as seis bases dos sentidos? Qual a condição para as seis bases dos sentidos?' Então, bhikkhus, empregando a atenção com sabedoria ocorreu a realização através da sabedoria: 'Quando há a mentalidade-materialidade (nome e forma), as seis bases dos sentidos surgem; as seis bases dos sentidos têm a mentalidade-materialidade (nome e forma) como condição.'</p>

<p class=Normal>"Então, bhikkhus eu pensei: 'Quando existe o quê surge a mentalidade-materialidade (nome e forma)? Qual a condição para a mentalidade-materialidade (nome e forma)?' Então, bhikkhus, empregando a atenção com sabedoria ocorreu a realização através da sabedoria: 'Quando há a consciência, a mentalidade-materialidade (nome e forma) surge; a mentalidade-materialidade (nome e forma) tem a consciência como condição.'</p>

<p class=Normal>"Então, bhikkhus eu pensei: 'Quando existe o quê surge a consciência? Qual a condição para a consciência?' Então, bhikkhus, empregando a atenção com sabedoria ocorreu a realização através da sabedoria: 'Quando há as formações volitivas, a consciência surge; a consciência tem as formações volitivas como condição.'</p>

<p class=Normal>"Então, bhikkhus eu pensei: 'Quando existe o quê surgem as formações volitivas? Qual a condição para as formações volitivas?' Então, bhikkhus, empregando a atenção com sabedoria ocorreu a realização através da sabedoria: 'Quando há a ignorância, as formações volitivas surgem; as formações volitivas têm a ignorância como condição.'</p>

<p class=Normal>"Assim, com a ignorância como condição, as formações volitivas [surgem]; com as formações volitivas como condição, a consciência ... Essa é a origem de toda essa massa de sofrimento. </p>

<p class=Normal>"Originação, originação' - assim, bhikkhus, com relação a coisas não ouvidas antes, surgiram em mim a visão, a compreensão, a sabedoria, o verdadeiro conhecimento e a iluminação. </p>


<p class=Normal> <b>(Cessação)</b></p>

<p class=Normal>"Então, bhikkhus eu pensei: 'Quando não existe o quê não surge o envelhecimento e morte? Com a cessação do quê cessa o envelhecimento e morte?' Então, bhikkhus, empregando a atenção com sabedoria ocorreu a realização através da sabedoria: 'Quando não há o nascimento, o envelhecimento e morte não surgem; com a cessação do nascimento ocorre a cessação do envelhecimento e morte.'

<p class=Normal>"Então, bhikkhus eu pensei: 'Quando não existe o quê não surge o nascimento? Com a cessação do quê cessa o nascimento?' Então, bhikkhus, empregando a atenção com sabedoria ocorreu a realização através da sabedoria: 'Quando não há o ser/existir, o nascimento não surge; com a cessação do ser/existir ocorre a cessação  do nascimento.'</p>

<p class=Normal>"Então, bhikkhus eu pensei: 'Quando não existe o quê não surge o ser/existir? Com a cessação do quê cessa o ser/existir?' Então, bhikkhus, empregando a atenção com sabedoria ocorreu a realização através da sabedoria: 'Quando não há o apego, o ser/existir não surge; com a cessação do apego ocorre a cessação  do ser/existir.'</p>

<p class=Normal>"Então, bhikkhus eu pensei: 'Quando não existe o quê não surge o apego? Com a cessação do quê cessa o apego?' Então, bhikkhus, empregando a atenção com sabedoria ocorreu a realização através da sabedoria: 'Quando não há o desejo, o apego não surge; com a cessação do desejo ocorre a cessação  do apego.'  </p>

<p class=Normal>"Então, bhikkhus eu pensei: 'Quando não existe o quê não surge o desejo? Com a cessação do quê cessa o desejo?' Então, bhikkhus, empregando a atenção com sabedoria ocorreu a realização através da sabedoria: 'Quando não há a sensação, o desejo não surge; com a cessação da sensação ocorre a cessação  do desejo.'</p>

<p class=Normal>"Então, bhikkhus eu pensei: 'Quando não existe o quê não surge a sensação? Com a cessação do quê cessa a sensação?' Então, bhikkhus, empregando a atenção com sabedoria ocorreu a realização através da sabedoria: 'Quando não há o contato, a sensação não surge; com a cessação do contato ocorre a cessação  da sensação.'</p>

<p class=Normal>"Então, bhikkhus eu pensei: 'Quando não existe o quê não surge o contato? Com a cessação do quê cessa o contato?' Então, bhikkhus, empregando a atenção com sabedoria ocorreu a realização através da sabedoria: 'Quando não há as seis bases dos sentidos, o contato não surge; com a cessação das seis bases dos sentidos ocorre a cessação  do contato.'</p>

<p class=Normal>"Então, bhikkhus eu pensei: 'Quando não existe o quê não surgem as seis bases dos sentidos? Com a cessação do quê cessam as seis bases dos sentidos?' Então, bhikkhus, empregando a atenção com sabedoria ocorreu a realização através da sabedoria: 'Quando não há a mentalidade-materialidade (nome e forma), as seis bases dos sentidos não surgem; com a cessação da mentalidade-materialidade (nome e forma) ocorre a cessação  das seis bases dos sentidos.'</p>

<p class=Normal>"Então, bhikkhus eu pensei: 'Quando não existe o quê não surge a mentalidade-materialidade (nome e forma)? Com a cessação do quê cessa a mentalidade-materialidade (nome e forma)?' Então, bhikkhus, empregando a atenção com sabedoria ocorreu a realização através da sabedoria: 'Quando não há a consciência, a mentalidade-materialidade (nome e forma) não surge; com a cessação da consciência ocorre a cessação  da mentalidade-materialidade (nome e forma).'</p>

<p class=Normal>"Então, bhikkhus eu pensei: 'Quando não existe o quê não surge a consciência? Com a cessação do quê cessa a consciência?' Então, bhikkhus, empregando a atenção com sabedoria ocorreu a realização através da sabedoria: 'Quando não há as formações volitivas, a consciência não surge; com a cessação das formações volitivas ocorre a cessação  da consciência.'</p>

<p class=Normal>"Então, bhikkhus eu pensei: 'Quando não existe o quê não surgem as formações volitivas? Com a cessação do quê cessam as formações volitivas?' Então, bhikkhus, empregando a atenção com sabedoria ocorreu a realização através da sabedoria: 'Quando não há a ignorância, as formações volitivas não surgem; com a cessação da ignorância ocorre a cessação  das formações volitivas.'</p>

<p class=Normal>"Assim, do desaparecimento e cessação sem deixar vestígios dessa mesma ignorância cessam as formações volitivas; da cessação das formações volitivas cessa a consciência ... Essa é a cessação de toda essa massa de sofrimento. </p>

<p class=Normal>"Cessação, cessação - assim, bhikkhus, com relação a coisas não ouvidas antes, surgiram em mim a visão, a compreensão, a sabedoria, o verdadeiro conhecimento e a iluminação. </p>

