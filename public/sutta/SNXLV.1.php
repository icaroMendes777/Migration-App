<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim ouvi.
Em certa ocasião, o Abençoado estava em Savathi, no Bosque de Jeta no Parque de
Anathapindika. Lá ele se dirigiu aos monges desta forma: “Bhikkhus!”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável Senhor,” eles
responderam. O Abençoado disse o seguinte: <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus, a ignorância é a
precursora dos estados ruins e prejudiciais, seguida pela ausência de vergonha
de cometer transgressões e pela ausência de temor de cometê-las. Para um tolo,
imerso na ignorância, o entendimento incorreto surge. Naquele que possui o
entendimento incorreto, o pensamento incorreto surge. Naquele que possui o
pensamento incorreto, a linguagem incorreta surge. Naquele que possui a
linguagem incorreta, a ação incorreta surge. Naquele que possui a ação
incorreta, o modo de vida incorreto surge. Naquele que possui o modo de vida
incorreto, o esforço incorreto surge. Naquele que possui o esforço incorreto, a
atenção plena incorreta surge. Naquele que possui a atenção plena incorreta, a
concentração incorreta surge.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus, o verdadeiro
conhecimento é o precursor dos estados benéficos, seguido pela vergonha de
cometer transgressões e pelo temor de cometê-las. [<a href="#N1">1</a>] <a
name=R1></a>Para um sábio que alcançou o verdadeiro conhecimento, o
entendimento correto surge. Naquele que possui o entendimento correto, o
pensamento correto surge. Naquele que possui o pensamento correto, a linguagem
correta surge. Naquele que possui a linguagem correta, a ação correta surge.
Naquele que possui a ação correta, o modo de vida correto surge. Naquele que
possui o modo de vida correto, o esforço correto surge. Naquele que possui o
esforço correto, a atenção plena correta surge. Naquele que possui a atenção
plena correta, a concentração correta surge.”<o:p></o:p></span></p>


