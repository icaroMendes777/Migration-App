<p class=Normal> “Bhikkhus, esses quatro tempos, corretamente desenvolvidos e coordenados, gradualmente culminam na destruição das impurezas. Quais quatro? O tempo para ouvir o Dhamma, o tempo para discutir o Dhamma, o tempo para a compreensão, e o tempo para o insight. Esses quatro tempos, corretamente cultivados e realizados, gradualmente culminam na destruição das impurezas.</p>

<p class=Normal> “Tal qual quando chove e a chuva cai em gotas pesadas no alto das montanhas, a água flui pelas encostas, enchendo as fissuras, rachaduras e valas. Quando as fissuras, rachaduras e valas estão preenchidas, as gotas pesadas de chuva enchem as pequenas lagoas. Quando as pequenas lagoas estão preenchidas, elas enchem os grandes lagos. Quando os grandes lagos estão preenchidos, elas enchem os pequenos rios. Quando os pequenos rios estão preenchidos, elas enchem os grandes rios. Quando os grandes rios estão preenchidos, elas enchem o grande oceano; assim também, esses quatro tempos, corretamente desenvolvidos e coordenados, gradualmente culminam na destruição das impurezas." </p>

