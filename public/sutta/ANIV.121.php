<p class=Normal> “Bhikkhus, há esses quatro perigos. Quais quatro? O perigo de repreender a si mesmo, o perigo de ser repreendido pelos outros, o perigo da punição, e o perigo de um destino ruim. </p>

<p class=Normal> “E qual, bhikkhus, é o perigo de repreender a si mesmo? É o caso em que alguém reflete da seguinte forma: 'Se eu me ocupasse com a conduta corporal, verbal, e mental imprópria, eu não reprovaria a mim mesmo devido a esse comportamento?' Temendo o perigo de culpar a si mesmo ele abandona a conduta corporal imprópria e desenvolve a conduta corporal apropriada; ele abandona a conduta verbal imprópria e desenvolve a conduta verbal apropriada; ele abandona a conduta mental imprópria e desenvolve a conduta mental apropriada; ele se mantém puro. Esse é chamado o perigo de repreender a si mesmo. </p>

<p class=Normal> “E qual é o perigo de ser repreendido pelos outros? É o caso em que alguém reflete da seguinte forma: 'Se eu me ocupasse com a conduta corporal, verbal, e mental imprópria, eu não seria repreendido pelos outros devido a esse comportamento?' Temendo o perigo de ser repreendido pelos outros ele abandona a conduta corporal imprópria e desenvolve a conduta corporal apropriada; ele abandona a conduta verbal imprópria e desenvolve a conduta verbal apropriada; ele abandona a conduta mental imprópria e desenvolve a conduta mental apropriada; ele se mantém puro. Esse é chamado o perigo ser repreendido pelos outros. </p>

<p class=Normal> “E qual é o perigo da punição? É o caso em que alguém vê que quando os reis prendem um ladrão eles o submetem a vários tipos de castigos: eles são açoitados com chicotes, golpeados com varas, golpeados com clavas; as mãos são cortadas, os pés são cortados, as mãos e os pés são cortados; as orelhas são cortadas, o nariz é cortado, as orelhas e o nariz são cortados; eles são sujeitos ao ‘pote de mingau,’ ao ‘barbeado com a concha polida,’ à ‘boca de Rahu,’ à ‘grinalda ardente,’ à ‘mão ardente,’ às ‘lâminas de capim,’ à ‘túnica de casca de árvore,’ ao ‘antílope,’ aos ‘ganchos de carne,’ às ‘moedas,’ à ‘conserva em desinfetante’ ao ‘pino que gira,’ ao ‘colchão de palha enrolado’; eles são molhados com óleo fervente, atirados para serem devorados pelos cães, empalados vivos em estacas, decapitados com espadas. </p>

<p class=Normal>"Ele reflete da seguinte forma: 'Quando os reis prendem um ladrão eles o submetem a vários tipos de castigos: eles são açoitados com chicotes ... decapitados com espadas. Agora se eu praticasse esse tipo de ação ruim, e se os reis me prendessem, eles me sujeitariam aos mesmos castigos. Eles me açoitariam com chicotes .... decapitariam com espadas.' Temendo o perigo da punição, ele não  se ocupa com o roubo das posses dos outros. Esse é chamado o perigo da punição. </p>

<p class=Normal> “E qual é o perigo de um destino ruim? É o caso em que alguém reflete da seguinte forma: 'A conduta corporal imprópria produz resultados ruins nas vidas futuras; a conduta verbal imprópria produz resultados ruins nas vidas futuras; a conduta mental imprópria produz resultados ruins nas vidas futuras. Agora se eu me ocupasse com a conduta imprópria através do corpo, linguagem e mente, então na dissolução do corpo, após a morte, eu reapareceria num estado de privação, num destino infeliz, nos reinos inferiores, até mesmo no inferno.' Temendo o perigo de um destino ruim, ele abandona a conduta corporal imprópria e desenvolve a conduta corporal apropriada; ele abandona a conduta verbal imprópria e desenvolve a conduta verbal apropriada; ele abandona a conduta mental imprópria e desenvolve a conduta mental apropriada; ele se mantém puro. Esse é chamado o perigo de um destino ruim. </p>

