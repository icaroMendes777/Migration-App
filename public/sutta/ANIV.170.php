<p class=Normal><b style='mso-bidi-font-weight:normal'><span lang=PT-BR
style='mso-ansi-language:PT-BR'>Notas:<o:p></o:p></span></b></p>


<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] <i style='mso-bidi-font-style:normal'>Samatha-pubbangamam vipassanam</i>.
Isto se refere a um meditador que faz da tranqüilidade o veículo para a sua
prática, (<i style='mso-bidi-font-style:normal'>samatha-yanika</i>), isto é,
aquele que primeiro desenvolve a concentração para depois desenvolver a prática
de insight. [<a href="#R1">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[2] O “caminho”, (<i style='mso-bidi-font-style:normal'>magga</i>), é o
primeiro caminho supramundano, entrar na correnteza. Desenvolver o caminho
segundo AA significa praticar para realizar os caminhos supramundanos
superiores. [<a href="#R2">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[3] <i style='mso-bidi-font-style:normal'>Vipassana- pubbangamam
samatham. </i>AA: isto se refere ao meditador cuja inclinação natural é
primeiro desenvolver o insight e depois baseado no insight desenvolver a
concentração, (<i style='mso-bidi-font-style:normal'>samadhi</i>). Este meditador
faz do insight o veículo para a sua prática, (<i style='mso-bidi-font-style:
normal'>vipassana-yanika</i>). [<a href="#R3">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[4] <i style='mso-bidi-font-style:normal'>Samatha-vipassanam
yuganaddham. </i>Nesta forma de prática o meditador primeiro alcança o primeiro
jhana e depois aplica o insight a essa experiência. O mesmo se aplica os demais
jhanas, seguindo dessa forma até que o caminho de entrar na correnteza seja
realizado. [<a href="#R4">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N5></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[5] <i style='mso-bidi-font-style:normal'>Dhammudhacca-viggahitam
manasam hoti. </i>De acordo com o AA, a “inquietação”, (<i style='mso-bidi-font-style:
normal'>udhacca</i>), neste caso significa a reação ao surgimento das dez
corrupções do insight. Essas corrupções são descritas no Vsm XX.
Também é possível que a inquietação da mente com respeito ao Dhamma seja a
aflição causada pela ansiedade que algumas vezes pode precipitar a experiência
da iluminação instantânea. [<a href="#R5">Retorna</a>]<o:p></o:p></span> </p>

