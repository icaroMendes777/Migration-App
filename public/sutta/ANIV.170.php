<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Certa
ocasião o Ven. Ananda estava em Kosambi, no monastério de Ghosita. Lá ele se
dirigiu aos bhikkhus, “Amigos!” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim, amigo,” os bhikkhus
responderam.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O Ven. Ananda disse:
“Amigos, qualquer um – bhikkhu ou bhikkhuni – que declare ter alcançado o
estado de arahant na minha presença, todos eles assim o fizeram através de um
ou outro de quatro modos. Quais quatro? <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Há o caso em que um
bhikkhu desenvolve o insight precedido pela tranqüilidade. [<a href="#N1">1</a>]
<a name=R1></a>À medida que ele desenvolve o insight precedido pela
tranqüilidade, o caminho surge. Ele segue aquele caminho, o desenvolve e o
cultiva. Ao seguir o caminho, desenvolvê-lo e cultivá-lo – os seus grilhões são
abandonados, as suas tendências subjacentes são destruídas.[<a href="#N2">2</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Depois
há<span style='mso-spacerun:yes'>  </span>o caso em que um bhikkhu desenvolve a
tranqüilidade precedida pelo insight.[<a href="#N3">3</a>] <a name=R3></a>À
medida que ele desenvolve a tranqüilidade precedida pelo insight, o caminho
surge. Ele segue aquele caminho, o desenvolve e o cultiva. Ao seguir o caminho,
desenvolvê-lo e cultivá-lo – os seus grilhões são abandonados, as suas
tendências subjacentes são destruídas.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Depois há<span
style='mso-spacerun:yes'>  </span>o caso em que um bhikkhu desenvolve a
tranqüilidade em tandem com o insight. [<a href="#N4">4</a>] <a name=R4></a>À
medida que ele desenvolve a tranqüilidade em tandem com o insight, o caminho
surge. Ele segue aquele caminho, o desenvolve e o cultiva. Ao seguir o caminho,
desenvolvê-lo e cultivá-lo – os seus grilhões são abandonados, as suas
tendências subjacentes são destruídas.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Depois há o caso em que um
bhikkhu tem a inquietação da mente com respeito ao Dhamma bem sob controle. [<a href="#N5">5</a>] <a name=R5></a>Mas então ocore que a mente dele se
firma no interior, se estabiliza e se torna concentrada e unificada; e assim
surge o caminho. Ele segue aquele caminho, o desenvolve e o cultiva. Ao seguir
o caminho, desenvolvê-lo e cultivá-lo – os seus grilhões são abandonados, as
suas tendências subjacentes são destruídas. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Qualquer um – bhikkhu ou
bhikkhuni – que declare ter alcançado o estado de arahant na minha presença,
todos eles assim o fizeram através de um ou outro desses quatro modos.”<o:p></o:p></span></p>

