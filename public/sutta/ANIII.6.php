<p class=Normal>"Bhikkhus, um tolo é caracterizado por suas ações; um sábio é caracterizado por suas ações. A sabedoria resplandece nessa manifestação. </p>

<p class=Normal> "Bhikkhus, quem possui três qualidades deve ser conhecido como um tolo. Quais três? Conduta corporal prejudicial, conduta verbal prejudicial e conduta mental prejudicial. Quem possui essas três qualidades deve ser conhecido como um tolo. Quem possui três qualidades deve ser conhecido como um sábio. Quais três? Conduta corporal benéfica, conduta verbal benéfica e boa conduta mental benéfica. Quem possui essas três qualidades deve ser conhecido como um sábio. [<a href="#N1">1</a>] </p>

<p class=Normal>"Portanto, bhikkhus, vocês devem treinar assim: 'Nós evitaremos as três qualidades que possuídas fazem de alguém um tolo e nós adotaremos e manteremos as três qualidades que possuídas fazem de alguém um sábio.' Assim é como vocês devem treinar." </p>

