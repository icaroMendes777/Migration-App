sem deixar vestígios.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>22. “Tendo
alcançado aquela suprema atenção plena cuja pureza é devida à
equanimidade, esse nobre discípulo recorda das suas muitas vidas passadas, isto
é, um nascimento, dois nascimentos ... (igual ao </span><a href="MN51.php#T24"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>MN 51, verso 24</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>) ... Assim ele se recorda das suas
muitas vidas passadas nos seus modos e detalhes.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>23. “Tendo
alcançado aquela suprema atenção plena cuja pureza é devida à
equanimidade, com o olho divino, que é purificado e ultrapassa o humano, esse
nobre discípulo vê seres falecendo e renascendo, inferiores e superiores,<span
style='mso-spacerun:yes'>  </span>bonitos e feios, afortunados e desafortunados
... (igual ao </span><a href="MN51.php#T25"><span lang=PT-BR style='mso-ansi-language:
