<p class=Normal> Assim ouvi. Em Savatthi. Lá, o Abençoado disse o seguinte: “Bhikkhus, dediquem-se ao afastamento. Um bhikkhu afastado compreende as coisas como elas na verdade são. </p>

<p class=Normal> “E o que ele compreende como na verdade é? A origem e a cessação da forma; a origem e a cessação da sensação; a origem e a cessação da percepção; a origem e a cessação das formações volitivas; a origem e a cessação da consciência. </p>

<p class=Normal> “E qual, bhikkhus, é a origem da forma? Qual a origem da sensação? Qual a origem da percepção? Qual a origem das formações volitivas? Qual a origem da consciência? </p>

<p class=Normal> “Aqui, bhikkhus, alguém busca o prazer, alguém acolhe, alguém agarra. E no que é que alguém busca o prazer, o que é que alguém acolhe e o que é que alguém agarra? Alguém busca o prazer na forma, acolhe-a e agarra-a. Como conseqüência disso, o prazer surge. O prazer pela forma é o apego. Tendo o apego como condição, o ser/existir [surge]; com o ser/existir como condição, o nascimento; com o nascimento como condição, envelhecimento e morte, tristeza, lamentação, dor, angústia e desespero surgem. Essa é a origem de toda essa massa de sofrimento. </p>

<p class=Normal> “Alguém busca o prazer na sensação ... na percepção ... nas formações volitivas ... na consciência, acolhe-a e agarra-a. Como conseqüência disso, o prazer surge ... Essa é a origem de toda essa massa de sofrimento. </p>

<p class=Normal> “Essa, bhikkhus, é a origem da forma; essa é a origem da sensação; essa é a origem da percepção; essa é a origem das formações volitivas; essa é a origem da consciência. </p>

<p class=Normal> “E qual, bhikkhus, é a cessação da forma? Qual a cessação da sensação? Qual a cessação da percepção? Qual a cessação das formações volitivas? Qual a cessação da consciência? </p>

<p class=Normal> “Aqui, bhikkhus, alguém não busca o prazer, alguém não acolhe e alguém não agarra. E no que é que alguém não busca o prazer? O que alguém não acolhe? O que alguém não agarra? Alguém não busca o prazer na forma, não a acolhe e não a agarra. Como conseqüência disso, o prazer pela forma cessa. Com a cessação do prazer, cessa o apego; com a cessação do apego, cessa o ser/existir ... Essa é a cessação de toda essa massa de sofrimento. </p>

<p class=Normal> “Alguém não busca o prazer na sensação ... na percepção ... nas formações volitivas ... na consciência, não a acolhe e não a agarra. Como conseqüência disso, o prazer pela consciência cessa ... Essa é a cessação de toda essa massa de sofrimento. </p>

<p class=Normal> “Essa, bhikkhus, é a cessação da forma; essa é a cessação da sensação; essa é a cessação da percepção; essa é a cessação das formações volitivas; essa é a cessação da consciência.” </p>

