<p class=Normal> Em certa ocasião, o Abençoado estava com os Vajjis em Hatthigama. Lá o Abençoado se dirigiu aos bhikkhus: </p>

<p class=Normal>"Bhikkhus, vocês devem se recordar do chefe de família Ugga de Hatthigama como alguém que possui oito qualidades maravilhosas e admiráveis." Isso foi o que o Abençoado disse. Tendo dito isso, ele levantou do seu assento e foi para a sua moradia. </p>

<p class=Normal>Então, de manhã, um certo bhikkhu se vestiu, tomou a sua tigela e o manto externo e foi até a residência do chefe de família Ugga de Hatthigama. Ao chegar ele sentou num assento que havia sido preparado. Então o chefe de família Ugga de Hatthigama foi até o bhikkhu e depois de cumprimentá-lo sentou a um lado. O bhikkhu então disse: </p>

<p class=Normal>"Chefe de família, o Abençoado declarou que você possui oito qualidades maravilhosas e admiráveis. Quais são elas?" </p>

<p class=Normal>"Eu não sei, venerável senhor, quais são as oito qualidades maravilhosas e admiráveis que o Abençoado declarou que possuo. No entanto são encontradas em mim oito qualidades maravilhosas e admiráveis. Ouça e preste muita atenção àquilo que eu vou dizer." </p>

<p class=Normal>"Sim, chefe de família." O bhikkhu respondeu. O chefe de família Ugga de Hatthigama disse o seguinte: </p>

<p class=Normal> (1) "Venerável senhor, eu estava numa bebedeira no bosque Naga quando vi o Abençoado pela primeira vez vindo à distância. Assim que o vi a minha mente ficou satisfeita, clara, com serena confiança, e a minha embriaguez desapareceu. Essa é a primeira qualidade maravilhosa e admirável que é encontrada em mim. </p>

<p class=Normal> (2) "Com a minha mente satisfeita, clara, com serena confiança me aproximei do Abençoado. Então o Abençoado transmitiu o ensino gradual, isto é, ele falou sobre a generosidade, sobre a virtude, sobre o paraíso; ele explicou o perigo, a degradação e as contaminações dos prazeres sensuais e as vantagens da renúncia. Quando ele percebeu que a minha mente estava pronta, receptiva, livre de obstáculos, satisfeita, clara, com serena confiança, ele explicou o ensinamento particular dos Budas: o sofrimento, a sua origem, a sua cessação e o caminho. Tal como um pano limpo, com todas as manchas removidas, irá absorver um corante de modo adequado, assim também, enquanto eu estava ali sentado, a visão imaculada do Dhamma surgiu em mim: “Tudo que está sujeito ao surgimento está sujeito à cessação.” Eu vi o Dhamma, alcancei o Dhamma, compreendi o Dhamma, examinei a fundo o Dhamma; superei a dúvida, me libertei da perplexidade, conquistei a intrepidez, e me tornei independente dos outros na Revelação do Mestre. Exatamente ali busquei refúgio no Buda, no Dhamma, na Sangha, e assumi as regras de treinamento incluindo o celibato. [<a href="#N1">1</a>] <a name=R1></a>Essa é a segunda qualidade maravilhosa e admirável que é encontrada em mim. </p>

<p class=Normal> (3) Eu tinha quatro jovens esposas. Então me dirigi a elas e disse: "Irmãs, eu assumi as regras de treinamento incluindo o celibato. Se quiserem vocês podem desfrutar aqui da riqueza e realizar méritos, ou podem regressar para o seu círculo familiar, ou me avisem se quiserem ser dadas para um outro homem.' Minha esposa mais velha então disse: 'Jovem senhor, me dê para tal senhor.' Eu mandei buscar esse homem e com a mão esquerda tomei minha esposa e com a mão direita tomei o vaso cerimonial e dei a minha esposa para aquele homem. Mas mesmo enquanto estava dando a minha jovem esposa, eu não lembro de qualquer alteração que estivesse ocorrendo na minha mente. Essa é a terceira qualidade maravilhosa e admirável que é encontrada em mim. </p>

<p class=Normal> (4) Minha família é rica, mas a riqueza é compartilhada sem reservas com as pessoas virtuosas com bom caráter. Essa é a quarta qualidade maravilhosa e admirável que é encontrada em mim. </p>

<p class=Normal> (5) "Sempre que me ocupo com um bhikkhu, eu assim faço com respeito, não sem respeito. Se aquele venerável me ensina o Dhamma, eu ouço com respeito, não sem respeito. Se ele não me ensina o Dhamma, então eu lhe ensino o Dhamma. Essa é a quinta qualidade maravilhosa e admirável que é encontrada em mim. </p>

<p class=Normal> (6) "Não é incomum, quando a Sangha tenha sido convidada por mim para uma refeição, os devas relatarem: 'Aquele bhikkhu, chefe de família, está libertado de ambos os modos. Aquele está libertado através da sabedoria. Aquele toca com o corpo. Aquele tem o entendimento realizado. Aquele é libertado pela fé. Aquele  é um discípulo do Dhamma. Aquele é um discípulo pela fé. Aquele é virtuoso, com bom caráter. Aquele é imoral, com caráter ruim.' No entanto, quando estou servindo a Sangha, eu não recordo pensar: 'Que eu dê para este um pouco, que eu dê para este muito.' Ao invés disso eu dou com a mente equilibrada. Essa é a sexta qualidade maravilhosa e admirável que é encontrada em mim. </p>

<p class=Normal> (7) "Não é incomum que os devas venham e relatem: 'Chefe de família, o Dhamma é bem exposto pelo Abençoado.' Eu então digo para os devas: 'Quer vocês devas digam ou não digam, o Dhamma é de fato bem exposto pelo Abençoado.' No entanto, eu não recordo nenhum tipo de júbilo mental surgindo porque os devas vêm até mim ou porque converso com os devas. Essa é a sétima qualidade maravilhosa e admirável que é encontrada em mim. </p>

<p class=Normal> (8) "Se eu falecesse antes do Abençoado, não seria surpresa se o Abençoado declarasse de mim: 'Não há grilhão através do qual o chefe de família Ugga de Hatthigama poderia retornar a este mundo.' [<a href="#N2">2</a>] <a name=R2></a> Essa é a oitava qualidade maravilhosa e admirável que é encontrada em mim. </p>

<p class=Normal>"Essas, venerável senhor, são as oito qualidades maravilhosas e admiráveis que são encontradas em mim. Mas eu não sei quais são as oito qualidades maravilhosas e admiráveis que o Abençoado declarou que possuo." </p>

<p class=Normal>Então aquele bhikkhu, tendo recebido comida esmolada na residência do chefe de família Ugga de Hatthigama, levantou do seu assento e partiu. Depois de haver retornado, após a refeição, ele foi até o Abençoado e depois de cumprimentá-lo, sentou a um lado e relatou toda a conversa que teve com o chefe de família Ugga de Hatthigama. </p>

<p class=Normal> [O Abençoado disse:] "Muito bem, bhikkhu! Eu declarei que o chefe de família Ugga de Hatthigama possui essas mesmas oito qualidades maravilhosas e admiráveis que ele lhe explicou corretamente. Você deve se recordar do chefe de família Ugga de Hatthigama como aquele que possui essas oito qualidades maravilhosas e admiráveis."
 </p>

