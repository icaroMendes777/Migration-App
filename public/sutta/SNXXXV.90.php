<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
estar agitado é uma enfermidade, estar agitado é um tumor, estar agitado é uma
flecha. [<a href="#N1">1</a>] <a name=R1></a>Por conseguinte, bhikkhus, o
Tathagata permanece sem agitação, com a flecha removida. Então, bhikkhus, se um
bhikkhu desejar, ‘Que eu permaneça sem agitação, com a flecha removida!’ ele
não deveria conceber [a si mesmo como] o olho, não deveria conceber [a si mesmo como] no olho, não deveria conceber
[a si mesmo separado] do olho, não deveria conceber, ‘O olho é meu.’ [<a href="#N2">2</a>]<o:p></o:p></span></p>


<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Ele não deveria conceber [a si mesmo como] as formas ... a consciência no olho ... o
contato no olho ... e qualquer sensação que surja tendo o contato no olho como
condição - experimentada como prazer, dor ou nem prazer, nem dor - ele não deveria conceber [a si mesmo como] isso, ele não deveria conceber [a si mesmo como ] nisso, ele
não deveria conceber [a si mesmo separado] disso, ele não deveria conceber, ‘Isso é meu.’<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele não
deveria conceber [a si mesmo como] o ouvido ... Ele não deveria conceber [a si mesmo como] a mente ... os objetos
mentais ... a consciência na mente ... o contato na mente ... e qualquer
sensação que surja tendo o contato na mente como condição - experimentada como prazer, dor ou nem prazer, nem dor - ele não deveria
conceber [a si mesmo como] isso, ele não deveria conceber [a si mesmo como] nisso, ele não deveria conceber [a si mesmo separado] disso,
ele não deveria conceber, ‘Isso é meu.’<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele não
deveria conceber [a si mesmo como] o todo, ele não deveria conceber [a si mesmo como] no todo, ele não deveria
conceber [a si mesmo separado] do todo, ele não deveria conceber, ‘O todo é meu.’<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Visto que
