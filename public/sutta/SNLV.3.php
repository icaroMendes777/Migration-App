<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em certa
ocasião, o Abençoado estava<span style="mso-spacerun: yes">  </span>em
Rajagaha, no Bambual, no Santuário dos Esquilos. Agora, naquela ocasião o
discípulo leigo Dighavu estava atormentado, sofrendo e gravemente enfermo.
Então, o discípulo leigo Dighavu se dirigiu ao seu pai, o chefe de família
Jotika, desta forma: <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venha,
chefe de família, vá até o Abençoado e preste uma homenagem em meu nome com a
sua cabeça aos pés dele e diga:<span style="mso-spacerun: yes"> 
</span>‘Venerável senhor, o discípulo leigo Dighavu está atormentado, sofrendo
e gravemente enfermo; ele homenageia o Abençoado com a cabeça aos seus pés.’
Então diga: ‘Seria bom, venerável senhor, se o Abençoado viesse até a
residência do discípulo leigo Dighavu por compaixão.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim, meu
caro,” o chefe de família Jotika respondeu e foi até o Abençoado e depois de
cumprimentá-lo sentou a um lado e relatou a sua mensagem. O Abençoado concordou
em silêncio.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, o
Abençoado se vestiu e tomando a tigela e o manto externo foi até a residência
do discípulo leigo Dighavu. Ele então sentou num assento que havia sido
preparado e disse para o discípulo leigo Dighavu: “Eu espero que você esteja
melhorando, Dighavu, espero que você esteja confortável, espero que as suas
sensações de dor estejam diminuindo e não aumentando e que a sua diminuição,
não o seu aumento, seja evidente.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
Senhor, eu não estou melhorando, não me sinto confortável. Minhas sensações de
dor estão aumentando, não diminuindo, o seu aumento, não a sua diminuição é
evidente.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então
Dighavu, você deve praticar da seguinte forma: ‘Eu serei um dos que possui
perfeita claridade, serenidade e confiança no Buda assim: <a name=R3></a>“O Abençoado é<span
style="mso-spacerun: yes">  </span>... mestre de devas e humanos, desperto,
sublime.”<span style="mso-spacerun: yes">  </span>Eu serei um dos que possui
perfeita claridade, serenidade e confiança no Dhamma ... na Sangha ... Eu serei um dos que possui as
virtudes apreciadas pelos nobres – intactas ... que conduzem à concentração.’ É
dessa forma que você deve praticar.”[<a href="#N1">1</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Venerável senhor, quanto a esses quatro fatores daquele que entrou na
correnteza, que foram ensinados pelo Abençoado, essas coisas existem em mim e
eu vivo em conformidade com essas coisas. Pois, venerável senhor, eu tenho
perfeita claridade, serenidade e confiança no Buda ... no Dhamma ... na Sangha ...eu possuo as
virtudes apreciadas pelos nobres – intactas ... que conduzem à concentração.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
Dighavu, estabelecido nesses fatores daquele que entrou na correnteza, você
além disso deveria desenvolver outras seis coisas que compartilham do
verdadeiro conhecimento. Aqui, Dighavu, permaneça com a percepção da impermanência em todas as formações condicionadas, 
permaneça com a percepção do sofrimento naquilo que é impermanente,
permaneça com a percepção do não-eu naquilo que é sofrimento, permaneça com a percepção do abandono, 
permaneça com a percepção do desapego, permaneça com a percepção da cessação. É dessa forma que você deve praticar.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, quanto a essas seis coisas que compartilham do verdadeiro conhecimento
que foram ensinadas pelo Abençoado, essas coisas existem em mim e eu vivo em
conformidade com essas coisas. Pois, venerável senhor, eu permaneço
com a percepção da impermanência em todas as formações condicionadas, 
permaneço com a percepção do sofrimento naquilo que é impermanente,
permaneço com a percepção do não-eu naquilo que é sofrimento, permaneço com a percepção do abandono, 
permaneço com a percepção do desapego, permaneço com a percepção da cessação. No
entanto, venerável senhor, este pensamento me ocorre: ‘Depois que eu tiver
partido, que este chefe de família Jotika não sofra.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não se
preocupe com isso, estimado Dighavu. Venha agora, estimado Dighavu, preste
muita atenção àquilo que o Abençoado está lhe dizendo.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, o
Abençoado, tendo dado aquela exortação ao discípulo leigo Dighavu, levantou-se
do seu assento e partiu. Então, não muito tempo depois do Abençoado ter
partido, o discípulo leigo Dighavu morreu. Nessa ocasião, um grande número de
bhikkhus foram até o Abençoado e depois de cumprimentá-lo sentaram a um lado e
disseram: “Venerável senhor, aquele discípulo leigo chamado Dighavu, para quem
o Abençoado deu uma breve exortação, morreu. Qual é o seu destino, qual é o seu
futuro percurso?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
o discípulo leigo Dighavu era sábio. Ele praticava de acordo com o Dhamma e não
criava problemas por conta do Dhamma. Bhikkhus, com a destruição dos primeiros
cinco grilhões, o discípulo leigo Dighavu reaparecerá espontaneamente [nas
Moradas Puras] e lá realizará o parinibbana sem nunca mais retornar daquele
mundo.”<o:p></o:p></span></p>




