<p class=Normal> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: “Eu vi seres que – praticando a conduta corporal ruim, a conduta verbal ruim e a conduta mental ruim; que insultavam os nobres, que possuíam o entendimento incorreto, e que praticavam ações sob a influência do entendimento incorreto - na dissolução do corpo, após a morte, renasceram num estado de privação, num destino infeliz, nos reinos inferiores, até mesmo no inferno. Não é por ter ouvido isto de outros brâmanes e contemplativos que eu lhes digo que eu vi seres que – praticando a conduta corporal ruim ... na dissolução do corpo, após a morte, renasceram num estado de privação, num destino infeliz, nos reinos inferiores, até mesmo no inferno. É por saber por mim mesmo, tendo visto eu mesmo, entendido eu mesmo, que eu lhes digo que seres que – praticando a conduta corporal ruim ... na dissolução do corpo, após a morte, renasceram num estado de privação, num destino infeliz, nos reinos inferiores, até mesmo no inferno.”</p>

<p class=Normal style='margin-left:.75in'> Com a mente dirigida incorretamente, <br>
empregando a linguagem incorreta, <br>
agindo de modo incorreto: <br>
uma pessoa<br>
com pouco aprendizado, <br>
que não realiza méritos<br>
nesta vida tão curta, <br>
na dissolução do corpo, <br>
sendo tolo, <br>
renasce no inferno.</p>


