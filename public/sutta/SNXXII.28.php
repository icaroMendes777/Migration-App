<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, se não houvesse gratificação na forma, os seres não se
enamorariam dela; mas porque há gratificação na forma, os seres dela se
enamoram. Se não houvesse o perigo na forma, os seres não experimentariam o
desencantamento dela; mas porque há o perigo na forma, os seres experimentam o
desencantamento dela. Se não houvesse escapatória no caso da forma, os seres
não escapariam dela; mas porque há escapatória da forma, os seres dela escapam.
<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
se não houvesse gratificação na sensação ... na percepção ... nas formações volitivas ...
na consciência, os seres não se enamorariam dela ... mas porque há escapatória
da consciência, os seres dela escapam. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Enquanto,
bhikkhus, os seres não obtêm o conhecimento direto de como na verdade são a
gratificação, o perigo e a escapatória no caso dos cinco agregados
influenciados pelo apego, eles não escapam deste mundo com os seus devas,
maras e brahmas, esta população com seus contemplativos e brâmanes, seus
príncipes e<span style="mso-spacerun: yes">  </span>povo; eles não se tornam
desapegados dele, libertados dele, nem permanecem com uma mente livre de
limitações. Mas quando os seres obtêm o conhecimento direto de como na verdade
são a gratificação, o perigo e a escapatória no caso dos cinco agregados
influenciados pelo apego, então eles escapam deste mundo com ... seus príncipes
e<span style="mso-spacerun: yes">  </span>povo; eles se tornam desapegados
dele, libertos dele, e permanecem com uma mente livre de limitações.”<b
style='mso-bidi-font-weight:normal'><o:p></o:p></b></span></p>


