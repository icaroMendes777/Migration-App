<p class=Normal>Então o errante Vacchagotta foi até o Abençoado e depois da troca de saudações corteses e amáveis ele sentou a um lado e disse: “Mestre Gotama, eu ouvi que ‘O contemplativo Gotama diz o seguinte: “Apenas para mim devem ser dadas oferendas, não para os outros. Apenas para os meus discípulos devem ser dadas oferendas, não para os discípulos dos outros. Apenas aquilo que é dado para mim gera grandes frutos e não aquilo que é dado para os outros. Apenas aquilo que é dado para os meus discípulos gera grandes frutos e não aquilo que é dado para os discípulos dos outros.”’ Agora, aqueles que assim dizem, falam aquilo que foi dito pelo Mestre Gotama e não o deturpam com algo contrário aos fatos? Eles explicam de acordo com o Dhamma de tal modo que nada que possa dar margem à censura possa de forma legítima ser deduzido da declaração deles? Pois nós não queremos deturpar o Mestre Gotama.” </p>

<p class=Normal> “Vaccha, qualquer um que diga isso: ‘O contemplativo Gotama diz o seguinte: “Apenas para mim devem ser dadas oferendas ... Apenas aquilo que é dado para os meus discípulos gera grandes frutos e não aquilo que é dado para os discípulos dos outros,”’ não estão falando aquilo que foi dito por mim e estão me deturpando com algo contrário aos fatos. </p>

<p class=Normal> “Vaccha, qualquer um que  impeça um outro de dar uma oferenda cria três obstruções, três impedimentos. Quais três? Ele impede o doador de realizar uma ação meritória, ele impede que o recebedor receba a oferenda, e antes disso ele mina e danifica o seu próprio eu. Qualquer um que impeça um outro de dar uma oferenda cria essas três obstruções, esses três impedimentos. </p>

<p class=Normal> “Eu lhe digo, Vaccha, mesmo se uma pessoa jogar fora o enxágue de uma tigela ou xícara no lago ou lagoa de um vilarejo, pensando, ‘Que os animais que aqui vivem se alimentem disso,’ isso seria uma fonte de mérito, para não dizer daquilo que é dado para os seres humanos. Mas eu digo que aquilo que é dado para uma pessoa virtuosa gera grandes frutos, nem tanto aquilo que é dado para a pessoa não virtuosa. [<a href="#N1">1</a>] <a name="R1"> </a>A pessoa virtuosa abandonou cinco qualidades e possui outras cinco qualidades. </p> 

<p class=Normal> “Quais são as cinco qualidades que ela abandonou? Ela abandonou o desejo sensual … a má vontade … o torpor e a preguiça ... a inquietação e a ansiedade ... a dúvida. Essas são as cinco qualidades que ela abandonou. E quais são as cinco qualidades que ela possui? Ela possui o agregado de virtude de quem está além do treinamento ... o agregado da concentração de quem está além do treinamento …  o agregado da sabedoria de quem está além do treinamento ... o agregado da libertação de quem está além do treinamento ... o agregado do conhecimento e visão da libertação de quem está além do treinamento. Essas são as cinco qualidades que ela possui. </p>

<p class=Normal> “Eu lhe digo: Aquilo que é dado para alguém que abandonou essas cinco qualidades e que possui essas outras cinco qualidades, gera grandes frutos.” </p>

