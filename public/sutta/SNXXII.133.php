<p class=Normal> Em Benares, no Parque do Gamo em Isipatana. Sentado a um lado o Venerável Sariputta disse para o Venerável Mahakotthita: </p>

<p class=Normal> “Amigo Mahakotthita, dizem, ‘ignorância, ignorância.’ O que então, amigo, é ignorância e de que maneira alguém está imerso na ignorância?” </p>

<p class=Normal> “Aqui, amigo, a pessoa comum sem instrução não compreende como na verdade é a gratificação, o perigo e a escapatória no caso da forma, sensação, percepção, formações volitivas e consciência. Isso, amigo, é chamado ignorância e dessa maneira alguém está imerso na ignorância. </p>

<p class=Normal> “Amigo Mahakotthita, dizem, ‘verdadeiro conhecimento, verdadeiro conhecimento.’ O que então, amigo, é o verdadeiro conhecimento e de que maneira alguém alcança o verdadeiro conhecimento?” </p>

<p class=Normal> “Aqui, amigo, o nobre discípulo bem instruído compreende como na verdade é a gratificação, o perigo e a escapatória no caso da forma, sensação, percepção, formações volitivas e consciência. Isso, amigo, é chamado verdadeiro conhecimento e dessa maneira alguém alcança o verdadeiro conhecimento.”
