<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião o Abençoado estava em Savatthi, no palácio da mãe de
Migara, no Parque do Oriente. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. Naquela
ocasião – no décimo quinto dia do Uposatha, na noite de lua cheia [<a href="#N1">1</a>]
- <a name=R1></a>o Abençoado estava sentado ao ar livre, rrodeado pela Sangha
dos bhikkhus.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. Então um certo bhikkhu
levantou do seu assento, [<a href="#N2">2</a>] <a name=R2></a>arrumou o seu
manto superior sobre um ombro e juntando as mãos em uma
saudação respeitosa disse para o Abençoado:<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável senhor, eu faria
uma pergunta ao Abençoado acerca de um certo tema, se o Abençoado concedesse
uma resposta à minha pergunta”. – “Sente bhikkhu e pergunte o que quiser.”
Assim o bhikkhu sentou e disse ao Abençoado:<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. “Não são estes,
venerável senhor, os cinco agregados influenciados pelo apego; isto é, o
agregado da forma material influenciado pelo apego, o agregado da sensação
influenciado pelo apego, o agregado da percepção influenciado pelo apego, o
agregado das formações volitivas influenciado pelo apego, o agregado da consciência
influenciado pelo apego?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Esses, bhikkhus, são os
cinco agregados influenciados pelo apego; isto é, o agregado da forma material
influenciado pelo apego ... e o agregado da consciência influenciado pelo apego.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Dizendo, “Muito bem,
venerável senhor”, o bhikkhu ficou contente e satisfeito com as palavras do
Abençoado. Então ele lhe fez outra pergunta:<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. “Mas, venerável senhor,
esses cinco agregados influenciados pelo apego estão enraizados no que?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Esses cinco agregados
influenciados pelo apego estão enraizados no desejo, bhikkhu.” [<a href="#N3">3</a>]
<o:p></o:p></span></p>

<p class=Normal><a name=R3></a> 6</span><span
lang=PT-BR style='mso-ansi-language:PT-BR'>. “Venerável senhor, esse apego é a
mesma coisa que os cinco agregados influenciados pelo apego, ou esse apego é
algo separado dos cinco agregados influenciados pelo apego?” [<a href="#N4">4</a>]
<o:p></o:p></span></p>

<p class=Normal><a name=R4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Bhikkhu, esse apego não é nem a mesma coisa que os cinco agregados
influenciados pelo apego, nem esse apego é algo separado dos cinco agregados
influenciados pelo apego. Qualquer desejo e cobiça que exista, esse é o apego. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7.
“Mas, venerável senhor, pode haver variedade no desejo e cobiça em relação aos
cinco agregados influenciados pelo apego?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Pode
haver, bhikkhu,” o Abençoado disse. “Nesse caso, bhikkhu, alguém pensa o
seguinte: ‘Que a minha forma material seja assim no futuro; que a minha
sensação seja assim no futuro; que a minha
percepção seja assim no futuro; que as minhas formações volitivas sejam assim no futuro;
que a minha consciência seja assim no futuro.’ Dessa forma, existe variedade no
desejo e cobiça em relação aos cinco agregados influenciados pelo apego.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8.
“Mas, venerável senhor, de que forma o termo ‘agregados’ se aplica aos
agregados?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhu,
qualquer tipo de forma material, quer seja do passado, do futuro ou do
presente, interna ou externa, grosseira ou sutil, inferior ou superior, próxima
ou distante – esse é o agregado da forma material. Qualquer tipo de sensação, quer seja do passado,
... próxima ou distante – esse é o agregado da sensação. Qualquer tipo de
percepção, quer seja do passado, ... próxima ou distante – esse é o agregado da percepção. Qualquer
tipo de formações volitivas, quer sejam do passado, ... próximas ou distantes – esse é o agregado das formações volitivas.
Qualquer tipo de consciência, quer seja do passado, ... próxima ou distante – esse é o agregado da
consciência. É dessa forma bhikkhu que o termo ‘agregado’ se aplica aos
agregados.”<o:p></o:p></span></p>

<p class=Normal><a name=C9></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>9. “Qual é a causa e condição, venerável senhor, para a manifestação do
agregado da forma material? Qual é a causa e condição para a manifestação do
agregado da sensação ... do agregado da percepção ... do agregado das formações volitivas
... do agregado da consciência?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Os
quatro grandes elementos, bhikkhu, são a causa e condição para a manifestação
do agregado da forma material. Contato é a causa e condição para a manifestação
do agregado da sensação. Contato é a causa e condição para a manifestação do
agregado da percepção. Contato é a causa e condição para a manifestação do
agregado das formações volitivas. Mentalidade-materialidade (nome e forma) é a causa e condição para a
manifestação do agregado da consciência.” [<a href="#N5">5</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R5></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>10. “Venerável senhor, como que surge a idéia da identidade?” [<a
href="#N6">6</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“ Nesse caso, bhikkhu, uma pessoa comum sem instrução que não respeita
os nobres, que não é proficiente nem treinada no Dhamma deles, que não
respeita os homens verdadeiros, que não é proficiente nem treinada no Dhamma
deles, considera a forma material como sendo o eu, ou o eu como possuído de
forma material, ou a forma material como estando no eu, ou o eu como estando na
forma material. Ela considera a sensação como sendo o eu ... percepção como
sendo o eu ... formações volitivas como sendo o eu ... consciência como sendo o eu,<span
style="mso-spacerun: yes">  </span>ou o eu como possuído de consciência, ou a
consciência como estando no eu, ou o eu como estando na consciência. Assim é
como surge a idéia da identidade.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. “Mas, venerável senhor,
como que não surge a idéia da identidade?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Neste caso, bhikkhu, um
nobre discípulo bem instruído, que é proficiente e treinado no Dhamma
deles, que respeita os homens verdadeiros, que é proficiente e treinado no
Dhamma deles, não considera a forma material como sendo o eu, ou o eu como
possuído de forma material, ou a forma material como estando no eu, ou o eu
como estando na forma material. Ela não considera a sensação como sendo o eu
... não considera a percepção como sendo o eu ... não considera as formações volitivas
como sendo o eu ... não considera a consciência como sendo o eu,<span
style="mso-spacerun: yes">  </span>ou o eu como possuído de consciência, ou a
consciência como estando no eu, ou o eu como estando na consciência.. Assim é
como não surge a idéia da identidade”.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. “Qual, venerável senhor,
é a gratificação, qual o perigo e qual é a escapatória da forma material?<span
style="mso-spacerun: yes">  </span>Qual é a gratificação, qual é o perigo, qual
é a escapatória da sensação ... da percepção ... das formações volitivas ... da
consciência?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O prazer e a alegria,
bhikkhu, que surgem na dependência da forma material – essa é a gratificação da
forma material. A forma material é impermanente, insatisfatória<span
style="mso-spacerun: yes">  </span>e sujeita à mudança – esse é o perigo da
forma material. A remoção do desejo e a cobiça, o abandono do desejo e a cobiça
pela forma material – essa é a escapatória da forma material.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O prazer e a alegria que
surgem na dependência da sensação ... na dependência da percepção ... na
dependência das formações volitivas ... na dependência da consciência – essa é a
gratificação da consciência. A consciência é impermanente, insatisfatória e
sujeita à mudança – esse é o perigo da consciência. A remoção do desejo e a
cobiça, o abandono do desejo e a cobiça pela consciência – essa é a escapatória
da consciência.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><a name=T13></a>13. “Venerável senhor, como
alguém pode saber, como alguém pode ver, de modo que em relação a este corpo
com a sua consciência e todos os sinais externos, não exista a fabricação de um
eu, a fabricação de um meu e a tendência subjacente à presunção?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhu, qualquer tipo de
forma material, quer seja do passado, do futuro ou do presente, interna ou
externa, grosseira ou sutil, inferior ou superior, próxima ou distante – a
pessoa vê toda forma material como na verdade ela é, com correta sabedoria,
deste modo: ‘Isso não é meu, isso não sou eu, isso não é o meu eu.’ Qualquer
tipo de sensação ... Qualquer tipo de percepção ... Qualquer tipo de formações volitivas
... Qualquer tipo de consciência ... a pessoa vê toda consciência tal como na
verdade ela é, com correta sabedoria, deste modo: ‘Isso não é meu, isso não sou
eu, isso não é o meu eu.’ É quando alguém sabe e vê desse modo que, em relação
a este corpo com a sua consciência e todos os sinais externos, não existe a
fabricação de um eu, a fabricação de um meu e a tendência subjacente à
presunção.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14. Então, na mente de um
certo bhikkhu surgiu este pensamento: “Portanto, parece que, a forma material
não é o eu, a sensação não é o eu, a percepção não é o eu, as formações volitivas não são
o eu, a consciência não é o eu. Qual o eu então, que é afetado por ações
praticadas pelo não-eu?” [<a href="#N7">7</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R7></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o
Abençoado, conhecendo o pensamento na mente daquele bhikkhu, dirigiu-se aos
bhikkhus da seguinte forma: “É possível, bhikkhus, que alguma pessoa que aqui
está, desencaminhada, obtusa e ignorante, com a sua mente dominada pelo desejo,
possa pensar que ela pode superar a Doutrina do Mestre desta forma: ‘Portanto,
parece que, a forma material não é o eu ... a consciência não é o eu. Qual o eu
então, que é afetado por ações praticadas pelo não-eu?’ Agora, bhikkhus, vocês
foram treinados por mim através de perguntas em várias ocasiões com relação
a várias coisas. [<a href="#N8">8</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R8></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>15.
“Bhikkhus, o que vocês pensam? A forma material é permanente ou impermanente?”
– “Impermanente, venerável senhor.” – “Aquilo que é impermanente é sofrimento
ou<span style="mso-spacerun: yes">  </span>felicidade?” – “Sofrimento,
venerável senhor.” – “É adequado que aquilo que é impermanente, sofrimento e
está sujeito à mudança, seja considerado desta forma: ‘Isso é meu, isso sou eu,
isso é o meu eu’?” – “Não venerável senhor.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus, o que vocês
pensam: A sensação ... percepção ... formações volitivas ... consciência são permanentes
ou impermanentes?” – “Impermanentes venerável senhor.” - “Aquilo que é
impermanente é sofrimento ou<span style="mso-spacerun: yes"> 
</span>felicidade?” – “Sofrimento, venerável senhor.” – “É adequado que aquilo
que é impermanente, sofrimento e está sujeito à mudança, seja considerado desta
forma: ‘Isso é meu, isso sou eu, isso é o meu eu’?” – “Não venerável senhor.”<o:p></o:p></span></p>

<p class=Normal><a name=R12></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>16.
Portanto, bhikkhus, qualquer tipo de forma material, quer seja do passado, do
futuro ou do presente ... toda forma material deve ser vista como na verdade
ela é, com correta sabedoria, deste modo: ‘Isso não é meu, isso não sou eu,
isso não é o meu eu’. Qualquer tipo de sensação ...Qualquer tipo de percepção
...Qualquer tipo de formações volitivas ...Qualquer tipo de consciência ... toda
consciência deve ser vista como na verdade ela é, com correta sabedoria, deste modo:
‘Isso não é meu, isso não sou eu, isso não é o meu eu.’<span
style="mso-spacerun: yes">  </span><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17. “Vendo desse modo um
nobre discípulo bem instruído se desencanta com a forma material, se desencanta
com a sensação, se desencanta com a percepção, se desencanta com as formações volitivas,
se desencanta com a consciência.<o:p></o:p></span></p>

<p class=Normal><a name=R13></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>18.
“Desencantado, ele se torna desapegado. Através do desapego a sua mente é libertada.
Quando ela está libertada surge o conhecimento: ‘Libertada.’ Ele compreende
que: ‘O nascimento foi destruido, a vida santa foi vivida, o que deveria ser
feito foi feito, não há mais vir a ser a nenhum estado’.”<span
style="mso-spacerun: yes">  </span><o:p></o:p></span></p>

<p class=Normal><a name=R9></a><a name=R27></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>Isso foi o que disse o Abençoado. Os bhikkhus
ficaram satisfeitos e contentes com as palavras do Abençoado. Agora, enquanto
este discurso estava sendo dito, através do desapego, as mentes de sessenta
bhikkhus foram libertadas das impurezas.[<a href="#N9">9</a>]<o:p></o:p></span></p>


