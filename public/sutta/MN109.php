<p class=Normal><a name=N5></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[5] No agregado da forma material cada um dos quatro grandes elementos é
uma condição para os outros três e para a forma material derivada. Contato é
uma condição para cada um dos três agregados intermediários, e é dito que: “Com
o contato, bhikkhus, a pessoa sente, com o contato a pessoa intenciona, com o
