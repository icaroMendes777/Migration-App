<a href="DN26.php">Cakkavatti-Sihanada Sutta (DN 26) -
O Rugido do Leão ao Girar a Roda</a>. No início e no final do dicurso o Buda
exorta os bhikkhus a se ‘manterem no seu próprio domínio’ através da prática da
atenção plena. O discurso traça a visão do papel de um monarca que faz girar a
roda, (um governante justo e íntegro), e da evolução do mundo.</span><span
