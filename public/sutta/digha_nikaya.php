<p class=Tit2 align=center style='text-align:center'>Os Discursos Longos</p>

<hr size=1 width="100%" align=center>

<p class="Normal"><span lang=PT-BR mso-ansi-language:PT-BR'>O Digha Nikaya, ou &quot;Coleção
de Discursos Longos&quot;, (Pali <i style='mso-bidi-font-style:normal'>digha</i>
= &quot;longo&quot;), é a primeira divisão do Sutta Pitaka e consiste de trinta
e quatro suttas agrupados em três <i style='mso-bidi-font-style:normal'>vaggas</i>
ou divisões: <o:p></o:p></span></p>

 <p class=Normal style='margin-left:.75in'> <a href="#sila">Silakkhandha-vagga</a> -- A
     Divisão da Virtude (13 suttas = DN 1 a DN 13) </span><br>
 <a href="#maha">Maha-vagga</a> – A Grande
     Divisão (10 suttas = DN 14 a DN 23) </span><br>
 <a href="#pathika">Patika-vagga</a> – A
     Divisão Patika (11 suttas = DN 24 a DN 34) </p>


<p class="Normal"><span lang=PT-BR mso-ansi-language:PT-BR'>No ensaio <a
href="../arquivo_textos_theravada/samyutta_intro.php">Introdução ao Samyutta
Nikaya</a>, <a href="../arquivo_textos_theravada/ensaios_bps.php">Bhikkhu Bodhi</a>
descreve o papel do Digha Nikaya da seguinte forma: “A opinião acadêmica
predominante, fomentada pelos próprios textos, afirma que a principal base para
distinguir os quatro Nikayas é o tamanho dos suttas. Assim, os suttas mais
extensos estão contidos no Digha Nikaya, aqueles com extensão média no Majjhima
Nikaya e os discursos mais curtos estão distribuídos entre o Samyutta e o
Anguttara Nikaya. No entanto, num importante estudo pioneiro, a erudita em
Pali, Joy Manné, questionou a premissa de que só o tamanho explica as diferenças
entre os Nikayas. Ao comparar cuidadosamente os suttas do DN com aqueles do MN,
Manné concluiu que as duas coleções têm a intenção de atender dois propósitos
distintos dentro da revelação do Buda. Na opinião dela, o DN estaria
primordialmente voltado para o propósito de propaganda, para atrair conversos
para a nova religião e dessa forma estaria destinado principalmente para não
Budistas que possuem uma inclinação favorável em relação ao Budismo.” <o:p></o:p></span></p>

