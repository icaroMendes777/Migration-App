<a href="DN32.php">Atanatiya<span
style='mso-spacerun:yes'>  </span>Sutta (DN 32) - Os Versos Protetores Atanata</a>.
Este sutta é um paritta, um composto de versos protetores. Além deste sutta, no
Cânone também podem ser encontrados outros suttas com essa mesma característica
, como por exemplo o Ratana Sutta (Snp II.1), Khandha Paritta (AN IV.67),
Dhajagga Paritta (SN XI.3), e o Mora Paritta (Ja 159).<o:p></o:p></span></p>

