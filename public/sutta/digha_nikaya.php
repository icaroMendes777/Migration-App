<a href="DN31.php">Sigalovada Sutta (DN 31) -- Exortação para
Sigala/O código de Disciplina para a Pessoa Leiga</a>.<span
style='mso-spacerun:yes'>  </span>O código de disciplina para o chefe de
família, descrito pelo Buda para o leigo Sigala. Este sutta oferece valioso conselho
