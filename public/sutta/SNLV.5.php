<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, o Venerável
Sariputta foi até o Abençoado e depois de cumprimentá-lo sentou a um lado. O
Abençoado disse o seguinte: <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sariputta,
assim é dito: ‘Um fator para entrar na correnteza, um fator para entrar na
correnteza.’ O que então, Sariputta, é um fator para entrar na correnteza?<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Associar-se
com pessoas verdadeiras, venerável senhor, é um fator para entrar na
correnteza. Ouvir o verdadeiro Dhamma é um fator para entrar na correnteza.
Atenção com sabedoria é um fator para entrar na correnteza. Prática de acordo
com o Dhamma é um fator para entrar na correnteza.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Muito bem,
Sariputta! Associar-se com pessoas verdadeiras, Sariputta, é um fator para
entrar na correnteza. Ouvir o verdadeiro Dhamma é um fator para entrar na
correnteza. Atenção com sabedoria é um fator para entrar na correnteza. Prática
de acordo com o Dhamma é um fator para entrar na correnteza.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sariputta,
assim é dito: ‘A correnteza, a correnteza.’ O que então, Sariputta, é essa
correnteza?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Este Nobre
Caminho Óctuplo, venerável senhor, é a correnteza: isto é, entendimento
correto, pensamento correto, linguagem correta, ação correta, modo de vida
correto, esforço correto, atenção plena correta, concentração correta.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Muito bem,
