correto ... concentração correta.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sariputta,
assim é dito: ‘Aquele que entrou na correnteza, aquele que entrou na
correnteza.’ O que então, Sariputta, é aquele que entrou na correnteza?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aquele que
possui este Nobre Caminho Óctuplo, venerável senhor é aquele que entrou na
