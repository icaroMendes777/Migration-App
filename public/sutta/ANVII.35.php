<p class=Normal> “Bhikkhus, um amigo dotado com sete qualidades merece a sua amizade. Quais sete? Ele dá aquilo que é difícil de dar. Ele faz aquilo que é difícil de fazer. Ele aguenta pacientemente o que é difícil de aguentar. Ele revela os segredos dele para você. Ele preserva os seus segredos. Ele não o abandona quando você está em apuros. Quando você está destituído ele não o despreza. Um amigo dotado com essas sete qualidades merece a sua amizade.” </p>

