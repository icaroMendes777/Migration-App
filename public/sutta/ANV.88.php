<p class=Normal> "Bhikkhus, possuindo cinco qualidades, um bhikkhu sênior age em prejuízo de muitas pessoas, pela infelicidade de muitas pessoas, pela ruína, dano, e sofrimento de muitas pessoas, de devas e humanos. Quais cinco? </p>

<p class=Normal> "(1) Um bhikkhu sênior tem muita reputação e há muito tempo seguiu a vida santa. (2) Ele é bem conhecido e famoso e tem um séquito com muitas pessoas, incluindo chefes de família e monásticos. (3) Ele ganha mantos, comida esmolada, moradia e medicamentos para os enfermos. (4) Ele aprendeu muito, se recorda daquilo que aprendeu e consolida aquilo que aprendeu. Aqueles ensinamentos que são admiráveis no início, admiráveis no meio, admiráveis no final, com o correto significado e fraseado e que revelam uma vida santa que é completamente perfeita e imaculada – ensinamentos como esses ele os aprendeu bem, se recorda, domina com a linguagem, investigou com a mente e penetrou corretamente com o entendimento. (5) Ele tem o entendimento incorreto e uma perspectiva distorcida. </p>

<p class=Normal> "Ele afasta muitas pessoas do bom Dhamma e as estabelece no Dhamma ruim. Pensando, 'O bhikkhu sênior tem muita reputação e há muito tempo seguiu a vida santa,' eles seguem o seu exemplo. Pensando, 'Ele é bem conhecido e famoso e tem um séquito com muitas pessoas, incluindo chefes de família e monásticos,' eles seguem o seu exemplo. Pensando, 'Ele ganha mantos, comida esmolada, moradia e medicamentos para os enfermos,' eles seguem o seu exemplo. Pensando, 'Ele aprendeu muito, se recorda daquilo que aprendeu e consolida aquilo que aprendeu,' eles seguem o seu exemplo. </p>

<p class=Normal> "Possuindo essas cinco qualidades, um bhikkhu sênior age em prejuízo de muitas pessoas, pela infelicidade de muitas pessoas, pela ruína, dano, e sofrimento de muitas pessoas, de devas e humanos. </p>

<p class=Normal> "Bhikkhus, possuindo outras cinco qualidades, um bhikkhu sênior age em benefício de muitas pessoas, pela felicidade de muitas pessoas, pelo bem, bem-estar, e felicidade de muitas pessoas, de devas e humanos. Quais cinco? </p>

