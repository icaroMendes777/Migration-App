<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(11. Os
Quatro Jhanas)</b><o:p></o:p></span></p>



<p class=Normal><a name=T25></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>25. “Outra vez, Udayin, eu proclamei para os meus discípulos o caminho
para desenvolver os quatro jhanas. Aqui, um bhikkhu afastado dos prazeres sensuais, afastado das qualidades não hábeis, entra e permanece no primeiro jhana, que é caracterizado pelo pensamento aplicado e sustentado, com o êxtase e felicidade nascidos do afastamento. Ele permeia e impregna, cobre e preenche o corpo com o êxtase e felicidade nascidos do afastamento. Não há nada em todo o corpo que não esteja permeado pelo êxtase e felicidade nascidos do afastamento. [<a href="#N9">9</a>] <a name=R9></a> É como se um banhista habilidoso ou seu aprendiz vertesse pó de banho numa bacia de latão e o misturasse, borrifando com água de tempos em tempos, de forma que essa bola de pó de banho - saturada, carregada de umidade, permeada por dentro e por fora - no entanto não pingasse; assim, o bhikkhu permeia, cobre e preenche o corpo com o êxtase e felicidade nascidos do afastamento....<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>26. “Outra
