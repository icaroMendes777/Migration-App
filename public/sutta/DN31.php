<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>29. &quot;De cinco formas
um pupilo deve servir ao seu mestre como o Sul: <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:PT-BR'>(i)
