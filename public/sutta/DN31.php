<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:PT-BR'>Com
a riqueza assim obtida o leigo pode<br>
dedicar-se ao bem dos seus,<br>
ele deve dividir a sua fortuna em quatro:<br>
(Isso irá trazer mais benefícios). <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:PT-BR'>Uma
porção ele usa para as suas necessidades, </span>[<a href="#N7"><span
