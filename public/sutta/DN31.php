<b style='mso-bidi-font-weight:normal'>(3)</b> ele dá bons conselhos,<br>
<b style='mso-bidi-font-weight:normal'>(4)</b> ele se compadece. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR;mso-bidi-font-weight:bold'>22.</span><b
