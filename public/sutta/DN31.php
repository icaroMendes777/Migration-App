<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:PT-BR'>(i)
ela organiza o trabalho da forma adequada,<br>
(ii) ela é gentil com os criados [</span><a href="#N10"><span lang=PT-BR
