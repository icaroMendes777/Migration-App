<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:PT-BR'>(i)
ele aprova as más ações do seu amigo,<br>
(ii) ele desaprova as boas ações do seu amigo,<br>
(iii) ele o elogia apenas na sua presença,<br>
(iv) ele fala mal na sua ausência. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR;mso-bidi-font-weight:bold'>19.</span><b
