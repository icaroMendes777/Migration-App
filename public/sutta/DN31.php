<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:PT-BR'>(i)
honrando-a,<br>
(ii) não depreciando-a,<br>
(iii) sendo-lhe fiel,<br>
(iv) dando-lhe autoridade,<br>
(v) dando-lhe ornamentos. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;De cinco formas, a
