o amigo na alegria e na tristeza,<br>
o amigo que dá bons conselhos,<br>
o amigo que se compadece - <br>
esses quatro tipos de amigos o sábio <br>
deve neles reconhecer o seu verdadeiro valor<br>

<span lang=PT-BR style='mso-ansi-language:
