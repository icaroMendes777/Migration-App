<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim falou o Abençoado. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>26. E tendo dito isso, o
