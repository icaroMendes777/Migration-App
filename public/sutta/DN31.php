<a name=T10></a>(iii) ela é fiel,<br>
(iv) ela protege o que ele traz,<br>
(v) ela é habilidosa e diligente em tudo que faz. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Dessa forma o Oeste está
coberto, fazendo com que esteja em paz e livre do medo <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>31. &quot;De cinco formas
