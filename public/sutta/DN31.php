<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:PT-BR'>(i)
ele o refreia de fazer o mal,<br>
(ii) ele o encoraja a fazer o bem,<br>
(iii) ele o informa acerca daquilo que ele não sabe,<br>
(iv) ele aponta o caminho para o paraíso. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR;mso-bidi-font-weight:bold'>25.</span><b
