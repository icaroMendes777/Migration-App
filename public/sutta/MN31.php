<p class=Tit3 align=center style='text-align:center'><b> Majjhima Nikaya 31 </b></Tit3>
<p class=Tit1 align=center style='text-align:center'>Culagosinga Sutta </Tit1>
<p class=Tit1 align=center style='text-align:center'>O Pequeno Discurso em Gosinga </Tit1>

