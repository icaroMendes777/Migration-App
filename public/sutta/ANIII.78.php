<p class=Normal>Então o Ven. Ananda foi até o Abençoado e
ao chegar, depois de cumprimentá-lo, sentou a um lado e o Abençoado disse:</p>


<p class=Normal>“Ananda, toda prática e preceito, toda vida, toda vida
santa, que é seguida como tendo um valor essencial:<span style="mso-spacerun:
yes">  </span>todas elas são proveitosas?” </p>

<p class=Normal>“Senhor, isso não deve ser respondido de forma categórica.” </p>

<p class=Normal>“Nesse caso, Ananda, dê uma resposta analítica.” </p>

<p class=Normal>“Quando – por seguir uma vida de prática e preceito, uma vida, uma vida
santa, que é seguida como tendo um valor essencial – as qualidades mentais
inábeis e prejudiciais aumentam enquanto que as qualidades mentais hábeis e
benéficas declinam: esse tipo de prática e preceito, vida, vida santa, que é
seguida como tendo um valor essencial é infrutífera. Mas quando - por seguir
uma vida de prática e preceito, uma vida, uma vida santa, que é seguida como
tendo um valor essencial – as qualidades mentais inábeis e prejudiciais declinam
enquanto que as qualidades mentais hábeis e benéficas aumentam: esse tipo de
prática e preceito, vida, vida santa, que é seguida como tendo um valor
essencial é frutífera.” </p>

<p class=Normal>Isso foi o que o Ven. Ananda disse, e o Mestre aprovou. Então, o Ven.
Ananda, pensando<span style="mso-spacerun: yes">  </span>“O Mestre me aprovou,”
levantou do seu assento e depois de homenagear o Abençoado, mantendo-o à sua
direita, partiu. </p>

<p class=Normal>Então não muito tempo depois que o <a name=PVW>Ven</a>. Ananda havia
partido, o Abençoado disse para os bhikkhus, “Bhikkhus, Ananda ainda está em
treinamento, mas não seria fácil encontrar alguém com a mesma sabedoria que
ele.”</p>

