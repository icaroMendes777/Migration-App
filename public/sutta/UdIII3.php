<p class=Normal>Assim ouvi. Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no Parque de Anathapindika.</p>



<p class=Normal>Agora, naquela ocasião quinhentos bhikkhus liderados pelo venerável Yasoja vieram até Savatthi para ver o Abençoado. Enquanto os bhikkhus visitantes estavam cumprimentando os bhikkhus residentes, estavam preparando os lugares para descanso, guardando as suas tigelas e mantos externos, eles faziam muito ruído e barulho.</p>



<p class=Normal>Então o Abençoado se dirigiu ao venerável Ananda assim: “Ananda, quem são essas pessoas ruidosas e barulhentas? Alguém poderia pensar que eles são pescadores apregoando peixes.”</p>



<p class=Normal>"Venerável senhor, eles são os quinhentos bhikkhus liderados pelo venerável Yasoja que vieram até Savatthi para ver o Abençoado. Conforme os bhikkhus visitantes cumprimentam os bhikkhus residentes, preparam os lugares para descanso, guardam as suas tigelas e mantos externos, eles fazem muito ruído e barulho.</p>



<p class=Normal>“Então, Ananda, diga a esses bhikkhus em meu nome que o Mestre chama os veneráveis.”</p>

 

<p class=Normal>“Sim, venerável senhor,” ele respondeu e foi até aqueles bhikkhus e lhes disse: “O Mestre chama os veneráveis.”</p>

<p class=Normal>“Sim, amigo,” eles responderam e foram até o Abençoado e depois de cumprimentá-lo sentaram a um lado. Estando sentados o Abençoado lhes perguntou:</p> <p class=Normal>“Bhikkhus porque vocês são tão ruidosos e barulhentos? Alguém poderia pensar que vocês são pescadores apregoando peixes.”</p>



<p class=Normal>Quando isto foi dito, o venerável Yasoja disse ao Abençoado:</p>



<p class=Normal>“Venerável senhor, estes quinhentos bhikkhus vieram até Savatthi para ver o Abençoado. E foi enquanto eles cumprimentavam os bhikkhus residentes, preparavam os lugares para descanso, guardavam as suas tigelas e mantos externos, que fizeram muito ruído e barulho.”</p>



<p class=Normal>Vão, bhikkhus, eu os dispenso. Vocês não podem estar comigo.”</p>



<p class=Normal>“Sim, venerável senhor,” eles responderam e se levantaram dos seus assentos e depois de homenagear o Abençoado, mantendo-o à sua direita, guardaram as coisas nos seus devidos lugares e tomando a tigela e o manto externo, partiram em direção ao território Vajji. Enquanto caminhavam em etapas por através do território Vajji eles foram até o rio Vaggumuda, e após se dirigirem à margem do rio fizeram cabanas de palha, e então entraram no retiro das chuvas.</p>



<p class=Normal>Então, o venerável Yasoja, tendo entrado no retiro das chuvas, disse para os bhikkhus: "Amigos, fomos dispensados pelo Abençoado, que deseja o melhor para nós e nosso bem-estar, e age por compaixão. Vamos então viver de tal maneira que o Abençoado se gratifique pelo nosso modo de vida.”</p>



<p class=Normal>"Sim, amigo," responderam aqueles bhikkhus ao venerável Yasoja. Então, durante o retiro das chuvas, enquanto aqueles bhikkhus permaneceram isolados, diligentes, ardentes e decididos, realizaram os três conhecimentos verdadeiros: o conhecimento das vidas passadas, o conhecimento sobre como o kamma determina o processo de renascimento e o conhecimento da destruição das impurezas mentais.</p>



<p class=Normal>Então, tendo permanecido em Savatthi pelo tempo desejado, o Abençoado saiu perambulando em direção a Vesali. Caminhando em etapas ele por fim chegou em Vesali, estabelecendo-se no Salão com um pico na cumeeira, na Grande Floresta.</p>



<p class=Normal>Então, abrangendo com a sua mente a mente dos bhikkhus que se encontravam à margem do rio Vaggamuda, o Abençoado disse ao venerável Ananda:</p> 



<p class=Normal>"Esta direção para mim parece luminosa, Ananda. Esta direção parece para mim deslumbrante. Não me causa repugnância direcionar e manter minha atenção na direção dos bhikkhus que se encontram instalados à margem do rio Vaggamuda. Envie um mensageiro até esses bhikkhus dizendo: "O Mestre os chama, amigos. O Mestre quer vê-los.”</p>



<p class=Normal>O venerável Ananda então respondeu, "Sim, venerável senhor.", foi até um certo bhikkhu e disse: "Venha agora, amigo. Vá até os bhikkhus que se encontram à margem do rio Vaggamuda e diga-lhes: "O Mestre os chama, amigos. O Mestre deseja vê-los.”</p>



<p class=Normal>Então, após responder "Sim, amigo." ao venerável Ananda o bhikkhu - como um homem forte que estende seu braço que está flexionado ou flexiona o braço que está estendido - desapareceu do Salão com um pico na Cumeeira, na Grande Floresta, e apareceu em frente aos bhikkhus instalados à margem do rio Vaggamuda. Então ele lhes disse: "O Mestre os chama, amigos. O Mestre quer vê-los.”</p>



<p class=Normal>Eles responderam "Sim, amigo" e em seguida colocaram em ordem seus alojamentos, tomaram as mantos e tigelas, e desapareceram da margem do rio Vaggamuda - como um homem forte que estende seu braço que está flexionado ou flexiona o braço que está estendido - e apareceram na presença do Abençoado no Salão com um pico na Cumeeira, na Grande Floresta.</p>



<p class=Normal>Agora, naquele momento, o Abençoado se encontrava sentado imerso na concentração imperturbável. Então, o pensamento ocorreu aos bhikkhus: "Qual morada da mente o Abençoado agora permanece?” E então eles compreenderam, "Ele se encontra numa morada imperturbável." Após isto, todos eles sentaram e também realizaram a concentração imperturbável.</p>



<p class=Normal>Então o Ven. Ananda – quando a noite já havia avançado, ao final da primeira vigília – levantou-se do seu assento, arrumou o seu manto sobre um ombro, ficou em pé de frente ao Abençoado com as mãos postas sobre o seu coração e lhe disse: “Venerável senhor a noite já avançou bastante. A primeira vigília terminou. Estes bhikkhus visitantes estão sentados aqui há tempo. Que o Abençoado lhes cumprimente." Quando isso foi dito o Abençoado permaneceu em silêncio.</p>



<p class=Normal>Em seguida, uma segunda vez, o Ven. Ananda disse: "A noite, venerável senhor, já avançou bastante. A segunda vigília terminou. Estes bhikkhus visitantes estão sentados aqui há tempo. Que o Abençoado lhes cumprimente." Quando isso foi dito o Abençoado permaneceu em silêncio.</p>



<p class=Normal>Em seguida, uma terceira vez, o Ven. Ananda disse: "A noite, venerável senhor, já avançou bastante. A terceira e última vigília terminou.  O amanhecer está surgindo e a noite desaparecendo. Estes bhikkhus visitantes estão sentados aqui há tempo. Que o Abençoado lhes cumprimente.”</p>



<p class=Normal>Então, o Abençoado, emergindo da concentração imperturbável, disse ao Ven. Ananda:</p>



<p class=Normal>"Ananda, se você soubesse, não teria dito o que disse sobre estes bhikkhus. Eu e todos estes quinhentos bhikkhus, Ananda, estávamos imersos na concentração imperturbável.”</p>



<p class=Normal>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p>



	<p class=Normal style='margin-left:.75in'>"O bhikkhu que venceu os espinhos dos desejos sensuais,<br>

	do insulto, da agressão e do aprisionamento:<br>

	tal como uma montanha firme e imperturbável,<br>

	ele não se agita diante do prazer e da dor.”</p>


