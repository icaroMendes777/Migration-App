<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[2] Não é possível dizer que uma pessoa é pura simplesmente porque ela
possui <span class=GramE>um certo</span> tipo de entendimento, etc. A pureza
não é definida nesses termos. Mas por outro lado uma pessoa não obtém a pureza
através da ausência de entendimento, etc. Colocando essas duas sentenças
juntas, a mensagem é a seguinte: a pessoa usa o entendimento correto, tradição,
conhecimento, virtude e práticas como o caminho, um meio para alcançar a
pureza. Uma vez que esta tenha sido alcançada, ela deixa de lado o caminho,
pois a pureza interior, no seu sentido último, é algo que transcende os meios
através dos quais ela é alcançada. <o:p></o:p></span></p>



