<p class=Normal> (11) "Bhikkhus, eu não vejo nenhuma outra coisa como o sinal do atrativo, por conta do qual o desejo sensual que ainda não surgiu surge, e o desejo sensual que já surgiu aumenta e expande. Aquele que dá atenção sem sabedoria ao sinal do atrativo, o desejo sensual que não surgiu surge, e o desejo sensual que já surgiu aumenta e expande." </p>

<p class=Normal> (12) "Bhikkhus, eu não vejo nenhuma outra coisa como o sinal do repulsivo, por conta do qual a má vontade que ainda não surgiu surge, e a má vontade que já surgiu aumenta e expande. Aquele que dá atenção sem sabedoria ao sinal do repulsivo, a má vontade que não surgiu surge, e a má vontade que já surgiu aumenta e expande." </p>

<p class=Normal> (13) "Bhikkhus, eu não vejo nenhuma outra coisa como: descontentamento, letargia, estirar-se preguiçosamente, sonolência após as refeições, e letargia mental, por conta do qual a preguiça e torpor que ainda não surgiram surgem e a preguiça e torpor que já surgiram aumentam e expandem. Aquele com a mente letárgica, a preguiça e torpor que não surgiram surgem, e a preguiça e torpor que já surgiram aumentam e expandem." </p>

<p class=Normal> (14) "Bhikkhus, eu não vejo nenhuma outra coisa como uma mente inquieta, por conta da qual a inquietação e ansiedade que ainda não surgiram surgem, e a inquietação e ansiedade que já surgiram aumentam e expandem. Aquele com a mente inquieta, a inquietação e ansiedade que não surgiram surgem, e a inquietação e a ansiedade que já surgiram aumentam e expandem." </p>

<p class=Normal> (15) "Bhikkhus, eu não vejo nenhuma outra coisa como a atenção sem sabedoria, por conta da qual a dúvida que ainda não surgiu surge, e a dúvida que já surgiu aumenta e expande. Aquele que dá atenção sem sabedoria, a dúvida que não surgiu surge, e a dúvida que já surgiu aumenta e expande." </p>

<p class=Normal> (16) "Bhikkhus, eu não vejo nenhuma outra coisa como o sinal do repulsivo, por conta do qual o desejo sensual que ainda não surgiu não surge, e o desejo sensual que já surgiu é abandonado. Aquele que dá atenção com sabedoria ao sinal do repulsivo o desejo sensual que não surgiu não surge, e o desejo sensual que já surgiu é abandonado." </p>

<p class=Normal> (17) "Bhikkhus, eu não vejo nenhuma outra coisa como a libertação da mente através do amor bondade, por conta da qual a má vontade que ainda não surgiu não surge, e a má vontade que já surgiu é abandonada. Aquele que dá atenção com sabedoria para a libertação da mente através do amor bondade, a má vontade que não surgiu não surge, e a má vontade que já surgiu é abandonada." </p>

<p class=Normal> (18) "Bhikkhus, eu não vejo nenhuma outra coisa como o elemento do estímulo, o elemento da persistência, e o elemento do esforço, por conta do qual a preguiça e torpor que ainda não surgiram não surgem, e a preguiça e torpor que já surgiram são abandonadas. Aquele que desperta a energia, a preguiça e torpor que não surgiram não surgem, e a preguiça e torpor que já surgiram são abandonadas." </p>

<p class=Normal> (19) "Bhikkhus, eu não vejo nenhuma outra coisa como uma mente quieta, por conta da qual a inquietação e ansiedade que ainda não surgiram não surgem, e a inquietação e ansiedade que já surgiram são abandonadas. Aquele com a mente quieta, a inquietação e ansiedade que não surgiram não surgem, e a inquietação e ansiedade que já surgiram são abandonadas." </p>

<p class=Normal> (20) "Bhikkhus, eu não vejo nenhuma outra coisa como a atenção com sabedoria, por conta da qual a dúvida que ainda não surgiu não surge, e a dúvida que já surgiu é abandonada. Aquele que dá atenção com sabedoria, a dúvida que não surgiu não surge, e dúvida que já surgiu é abandonada." </p>

