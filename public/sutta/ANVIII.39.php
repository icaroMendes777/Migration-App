<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
há essas oito fontes de méritos, fontes daquilo que é benéfico,
alimento da felicidade, celestiais, que resultam na felicidade, conduzem ao paraíso,
conduzem ao que é desejável, prazeroso e almejado, para o bem-estar e a
felicidade. Quais oito?<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“É o caso em que um nobre
discípulo buscou refúgio no Buda. Essa é a primeira fonte de méritos, fonte daquilo que é benéfico,
alimento da felicidade, celestial, que resulta na felicidade, conduz ao paraíso,
conduz ao que é desejável, prazeroso e almejado, para o bem-estar e a
felicidade. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Além disso, o nobre
discípulo buscou refúgio no Dhamma. Essa é a segunda fonte de méritos ... <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Além disso, o nobre
discípulo buscou refúgio na Sangha. Essa é a terceira fonte de méritos ...<span style='mso-spacerun:yes'>  </span><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Agora, há essas cinco
dádivas, cinco grandes dádivas - originais, que existem há muito tempo,
tradicionais, antigas, sem adulteração, não adulteradas desde o princípio - que
não estão sujeitas à suspeita, nunca estarão sujeitas à suspeita, e não são
criticáveis pelos sábios contemplativos e brâmanes. Quais cinco? <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“É o caso em que um
nobre discípulo, abandonando a destruição da vida, se abstém de tomar a
vida. Agindo assim, ele liberta do perigo, liberta da animosidade, liberta da
opressão um incontável número de seres. Libertando do perigo, libertando da
animosidade, libertando da opressão um incontável número de seres, ele ganha
uma parcela na ilimitada liberdade do perigo, liberdade da animosidade e
liberdade da opressão. Essa é a primeira dádiva, a primeira grande dádiva -
primordial, que existe há muito tempo, tradicional, antiga, sem adulteração e nunca antes
adulterada, que não está sendo adulterada e não será adulterada, que não é repudiada pelos sábios contemplativos e brâmanes. Essa é a quarta fonte de méritos ...<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Além disso, abandonando
tomar o que não é dado (roubar), o nobre discípulo se abstém de tomar o
que não é dado. Agindo assim, ele liberta do perigo, liberta da animosidade,
liberta da opressão um incontável número de seres. Libertando do perigo,
libertando da animosidade, libertando da opressão um incontável número de
seres, ele ganha uma parcela na ilimitada liberdade do perigo, liberdade da
animosidade e liberdade da opressão. Essa é a segunda dádiva … Essa é a quinta fonte de méritos ...<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Além disso, abandonando a
conduta sexual imprópria, o nobre discípulo se abstém da conduta sexual
imprópria. Agindo assim, ele liberta do perigo, liberta da animosidade, liberta
da opressão um incontável número de seres. Libertando do perigo, libertando da
animosidade, libertando da opressão um incontável número de seres, ele ganha
uma parcela na ilimitada liberdade do perigo, liberdade da animosidade e
liberdade da opressão. Essa é a terceira dádiva … Essa é a sexta fonte de méritos ...<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Além disso, abandonando a
mentira, o nobre discípulo se abstém de mentir. Agindo assim, ele liberta
do perigo, liberta da animosidade, liberta da opressão um incontável número de
seres. Libertando do perigo, libertando da animosidade, libertando da opressão
para um incontável número de seres, ele ganha uma parcela na ilimitada
liberdade do perigo, liberdade da animosidade, e liberdade da opressão. Essa é
a quarta dádiva … Essa é a sétima fonte de méritos ...<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Além disso,
abandonando tomar embriagantes, o nobre discípulo se abstém de
embriagantes. Agindo assim, ele liberta do perigo, liberta da animosidade,
liberta da opressão um incontável número de seres. Libertando do perigo,
libertando da animosidade, libertando da opressão um incontável número de
seres, ele ganha uma parcela na ilimitada liberdade do perigo, liberdade da
animosidade, e liberdade da opressão. Essa é a quinta dádiva, a quinta grande
dádiva - primordial, que existe há muito tempo, tradicional, antiga, sem adulteração e nunca antes
adulterada, que não está sendo adulterada e não será adulterada, que não é repudiada pelos sábios contemplativos e brâmanes. E essa é a oitava fonte de méritos, fonte daquilo que é benéfico,
alimento da felicidade, celestial, que resulta na felicidade, conduz ao paraíso,
conduz ao que é desejável, prazeroso e almejado, para o bem-estar e a
felicidade.&quot;<o:p></o:p></span></p>

