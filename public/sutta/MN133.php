<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. </span>Assim
ouvi. Em certa ocasião o Abençoado estava em Rajagaha<span lang=PT-BR
style='mso-ansi-language:PT-BR'> no Parque das Termas Quentes. Então, quando
estava próximo do amanhecer, o venerável Samiddhi foi até as termas quentes
para se banhar. Depois do banho ele saiu da água e ficou em pé vestido com um
manto, secando os membros. E assim, quando a noite estava bem avançada, um
certo deva com belíssima aparência que iluminou toda a área das Termas Quentes,
se aproximou do venerável Samiddhi. Ficando em pé a um lado, o deva disse:<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.
“Bhikkhu, você se lembra do sumário e da análise ‘Daquele que teve Uma Única
Noite Excelente’?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigo, eu
não me lembro do sumário e da análise ‘Daquele que teve Uma Única Noite
Excelente.’ Mas, amigo, você se lembra do sumário e da análise ‘Daquele que
teve Uma Única Noite Excelente’?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhu,
eu também não me lembro do sumário e da análise ‘Daquele que teve Uma Única
Noite Excelente.’ Mas, bhikkhu, você se lembra dos versos ‘Daquele que teve Uma
Única Noite Excelente’?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigo, eu
não me lembro dos versos ‘Daquele que teve Uma Única Noite Excelente.’ Mas,
amigo, você se lembra dos versos ‘Daquele que teve Uma Única Noite Excelente’?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhu,
eu também não me lembro dos versos ‘Daquele que teve Uma Única Noite
Excelente.’ Mas, bhikkhu, aprenda o sumário e a análise ‘Daquele que teve Uma
Única Noite Excelente.’ Bhikkhu, obtenha proficiência no sumário e na análise
‘Daquele que teve Uma Única Noite Excelente.’ Bhikkhu, lembre-se do sumário e da análise ‘Daquele que teve Uma Única Noite Excelente.’ Bhikkhu, o sumário e a análise ‘Daquele que teve Uma Única Noite Excelente’ traz benefício, faz
parte dos fundamentos da vida santa.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o
que o deva disse e em seguida desapareceu de vez.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. Então,
ao amanhecer, o venerável Samiddhi foi até o Abençoado. Depois de
cumprimentá-lo, ele sentou a um lado e relatou ao Abençoado tudo que havia
ocorrido, e disse: “Seria bom, venerável senhor, se o Abençoado me
ensinasse<span style="mso-spacerun: yes">  </span>o sumário e a análise
‘Daquele que teve Uma Única Noite Excelente.’”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. “Então,
bhikkhu, o</span>uça e preste muita atenção àquilo que eu vou dizer.” – “Sim,
venerável senhor,” <span lang=PT-BR style='mso-ansi-language:PT-BR'>o venerável
Samiddhi</span> respondeu. O Abençoado disse o seguinte<span lang=PT-BR
style='mso-ansi-language:PT-BR'>:<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. “Que
ninguém reviva o passado <br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>ou no
futuro coloque as suas esperanças; <br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>pois o
passado foi deixado para trás<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>e o futuro
ainda não foi alcançado.<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>Ao invés
disso, que ele veja com insight<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>cada estado
surgido no presente;<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>que ele
compreenda isso e tenha certeza disso,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>invencível,
inabalável.<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>Hoje o
esforço tem que ser feito;<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>amanhã a
Morte poderá vir, quem sabe?<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>Não há barganha
com a Mortalidade<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>que a
mantenha, com a sua horda, fora disso,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>mas aquele
que permanece assim ardente,<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>decidido,
durante o dia, durante a noite - <br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>é ele,
disse o Sábio em Paz; <br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>que teve
uma única noite excelente.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. Isso foi
o que o Abençoado disse. Tendo dito isso, ele levantou do seu assento e foi
para a sua moradia.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. Então,
pouco tempo depois do Abençoado ter partido, os bhikkhus consideraram: [<a
href="#N1">1</a>] <a name=R1></a>“Agora, amigos, o Abençoado se levantou do seu
assento e foi para a sua moradia depois de expor um sumário sem analisar o seu
significado em detalhe. Agora, quem irá analisar o significado em detalhe?”
Então eles consideraram: “O venerável Maha Kaccana é elogiado pelo Mestre e
estimado pelos seus sábios companheiros da vida santa. Ele é capaz de analisar
o significado em detalhe. E se fôssemos até ele e pedíssemos a explicação do
significado disso.”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. Então os
bhikkhus foram até o venerável Maha Kaccana e o cumprimentaram. Quando a
conversa cortês e amigável<span style="mso-spacerun: yes">  </span>havia
terminado, eles se sentaram num lado e contaram o que havia acontecido,
adicionando: &quot;Que o venerável Maha Kaccana nos explique isso.&quot;<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. [O
venerável Maha Kaccana respondeu:] “Amigos, é como se um homem que precisa de
madeira, procurasse madeira, perambulasse em busca de madeira, pensasse que a
madeira deveria ser procurada entre os galhos e as folhas de uma grande árvore
que possui madeira, depois de haver passado por cima da sua raiz e tronco. O
mesmo ocorre com vocês, veneráveis senhores, que pensam que eu deva ser
perguntado sobre o significado disso, depois de terem passado pelo Abençoado,
estando cara a cara com o Mestre. Pois, conhecer, o Abençoado conhece; ver, ele
vê; ele é visão, ele é conhecimento, ele é o Dhamma, ele é o sagrado; ele é o
que diz, o que proclama, o que elucida o significado, o que provê o imortal, o
senhor do Dhamma, o Tathagata. Aquele foi o momento quando vocês<span
style="mso-spacerun: yes">  </span>deveriam ter perguntado ao Abençoado o
significado. Aquilo que ele dissesse vocês deveriam se lembrar.”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10.
“Certamente, amigo Kaccana, conhecer, o Abençoado conhece; ver, ele vê; ele é
visão … o Tathagata. Aquele foi o momento quando nós deveríamos ter perguntado
ao Abençoado o significado. Aquilo que ele nos dissesse nós deveríamos nos
lembrar. No entanto, o venerável Maha Kaccana é elogiado pelo Mestre e estimado
pelos seus sábios companheiros da vida santa. O venerável Maha Kaccana é capaz
de expor o significado, em detalhe, desse sumário dito pelo Abençoado. Que o
venerável Maha Kaccana possa expor isso, sem que isso seja um problema.”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. “Então,
amigos, ouçam e prestem muita atenção àquilo que eu vou dizer.” – “Sim, amigo,”
os bhikkhus responderam. O venerável Maha Kaccana disse o seguinte:<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12.
“Amigos, quando o Abençoado se levantou do seu assento e foi para a sua moradia
depois de expor um sumário sem analisar o seu significado em detalhe, isto é: :<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:PT-BR'>‘Que
ninguém reviva o passado ...<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>que teve
uma única noite excelente,’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>eu entendo
que o significado em detalhe é o seguinte:<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. “Como,
amigos, uma pessoa revive o passado? Pensando, ‘Meu olho e as formas eram assim
no passado,’ [<a href="#N2">2</a>] <a name=R2></a>a consciência dela fica atada
pelo desejo e cobiça por aquilo. Devido à consciência dela estar atada pelo
desejo e cobiça, a pessoa se delicia com aquilo. Quando ela se delicia com
aquilo, ela revive o passado. <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Pensando,
‘Meu ouvido e os sons eram assim no passado ... Meu nariz e os aromas ... Minha
língua e os sabores ... Meu corpo e os tangíveis ... Minha mente e os objetos
mentais eram assim no passado,’ a consciência dela fica atada pelo desejo e
cobiça por aquilo. Devido à consciência dela estar atada pelo desejo e cobiça,
a pessoa se delicia com aquilo. Quando ela se delicia com aquilo, ela revive o
passado.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14. “Como
uma pessoa não revive o passado? Pensando, ‘Meu olho e as formas eram assim no
passado,’ a consciência dela não fica atada pelo desejo e cobiça por aquilo.
Devido à consciência dela não estar atada pelo desejo e cobiça, a pessoa não se
delicia com aquilo. Quando ela não se delicia com aquilo, ela não revive o
passado.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Pensando,
‘Meu ouvido e os sons eram assim no passado<span style="mso-spacerun: yes"> 
</span>... Meu nariz e os aromas ... Minha língua e os sabores ... Meu corpo e
os tangíveis ... Minha mente e os objetos mentais eram assim no passado,’ a
consciência dela não fica atada pelo desejo e cobiça por aquilo. Devido à
consciência dela não estar atada pelo desejo e cobiça, a pessoa não se delicia
com aquilo. Quando ela não se delicia com aquilo, ela não revive o passado.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. “E como,
amigos, uma pessoa coloca as suas esperanças no futuro? Pensando, ‘Que meu olho
e as formas sejam assim no futuro,’ ela empenha o seu coração em obter aquilo
que ainda não foi alcançado. Como ela assim empenha o seu coração, ela se
delicia com aquilo. Quando ela se delicia com aquilo, ela coloca as suas
esperanças no futuro.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Pensando,
‘Que meu ouvido e os sons sejam assim no futuro<span style="mso-spacerun:
yes">  </span>... Meu nariz e os aromas ... Minha língua e os sabores ... Meu
corpo e os tangíveis ... Minha mente e os objetos mentais sejam assim no
futuro,’ ela empenha o seu coração em obter aquilo que ainda não foi alcançado.
Como ela assim empenha o seu coração, ela se delicia com aquilo. Quando ela se
delicia com aquilo, ela coloca as suas esperanças no futuro.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16. “E
como, amigos, uma pessoa não coloca as suas esperanças no futuro? Pensando,
‘Que meu olho e as formas sejam assim no futuro,’ ela não empenha o seu coração
em obter aquilo que ainda não foi alcançado. Como ela assim não empenha o seu
coração, ela não se delicia com aquilo. Quando ela não se delicia com aquilo,
ela não coloca as suas esperanças no futuro.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Pensando,
‘Que meu ouvido e os sons sejam assim no futuro<span style="mso-spacerun:
yes">  </span>... Meu nariz e os aromas ... Minha língua e os sabores ... Meu
corpo e os tangíveis ... Minha mente e os objetos mentais sejam assim no
futuro,’ ela não empenha o seu coração em obter aquilo que ainda não foi
alcançado. Como ela assim não empenha o seu coração, ela não se delicia com
aquilo. Quando ela não se delicia com aquilo, ela não coloca as suas esperanças
no futuro.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17. “E
como, bhikkhus, uma pessoa é derrotada em relação aos estados que tenham
surgido no presente? Em relação ao olho e às formas que surgem no presente, a
consciência dela fica atada pelo desejo e cobiça por aquilo que surge no
presente. Devido à consciência dela estar atada pelo desejo e cobiça, a pessoa
se delicia com aquilo. Quando ela se delicia com aquilo, ela é derrotada em
relação aos estados que tenham surgido no presente.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em relação
ao ouvido e aos sons que surgem no presente ... ao nariz e aos aromas ... à
língua e aos sabores ... ao corpo e aos tangíveis ... à mente e aos objetos
mentais<span style="mso-spacerun: yes">  </span>que surgem no presente, a
consciência dela fica atada pelo desejo e cobiça por aquilo que surge no
presente. Devido à consciência dela estar atada pelo desejo e cobiça, a pessoa
se delicia com aquilo. Quando ela se delicia com aquilo, ela é derrotada em
relação aos estados que tenham surgido no presente.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18.<span
style="mso-spacerun: yes">  </span>“E como uma pessoa é invencível em relação
aos estados surgidos no presente?<span style="mso-spacerun: yes">  </span>Em relação
ao olho e às formas que surgem no presente, a consciência dela não fica atada
pelo desejo e cobiça por aquilo que surge no presente. Devido à consciência
dela não estar atada pelo desejo e cobiça, a pessoa não se delicia com aquilo.
Quando ela não se delicia com aquilo, ela é invencível em relação aos estados
que tenham surgido no presente.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em relação
ao ouvido e aos sons que surgem no presente ... ao nariz e aos aromas ... à
língua e aos sabores ... ao corpo e aos tangíveis ... à mente e aos objetos
mentais<span style="mso-spacerun: yes">  </span>que surgem no presente, a
consciência dela não fica atada pelo desejo e cobiça por aquilo que surge no
presente. Devido à consciência dela não estar atada pelo desejo e cobiça, a
pessoa não se delicia com aquilo. Quando ela não se delicia com aquilo, ela é
invencível em relação aos estados que tenham surgido no presente.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>19.
“Amigos, quando o Abençoado se levantou do seu assento e foi para a sua moradia
depois de expor um sumário sem analisar o seu significado em detalhe, isto é:<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:PT-BR'>‘Que
ninguém reviva o passado ...<br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'>que teve
uma única noite excelente,’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>é assim
como eu entendo o significado em detalhe. Agora, amigos, se vocês quiserem,
podem ir até o Abençoado perguntar-lhe qual o significado disso. Exatamente
aquilo que o Abençoado explicar é o que vocês deverão se lembrar.”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>20. Então
os bhikkhus, tendo se alegrado e se deliciado com as palavras do venerável Maha
Kaccana, levantaram dos seus assentos e foram até o Abençoado. Após
homenageá-lo, eles sentaram a um lado e relataram ao Abençoado aquilo que havia
ocorrido depois que ele havia partido, adicionando o seguinte: “Então,
venerável senhor, fomos até o venerável Maha Kaccana e lhe perguntamos sobre o
significado. O venerável Maha Kaccana nos explicou o significado com estes
termos, afirmações e frases.”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>21. “Maha
Kaccana é sábio, bhikkhus, Maha Kaccana possui muita sabedoria. Se vocês me
tivessem perguntado o significado, eu teria explicado da mesma forma que Maha
Kaccana explicou. Esse é o significado e é assim como vocês deverão se
lembrar.”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o
que disse o Abençoado. Os bhikkhus ficaram satisfeitos e contentes com as
palavras do Abençoado.<o:p></o:p></span></p>


