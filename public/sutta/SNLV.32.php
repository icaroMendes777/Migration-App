<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, há essas quatro fontes de méritos, fontes daquilo que é
benéfico, alimento da felicidade. Quais quatro?<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
bhikkhus, o nobre discípulo possui perfeita claridade, serenidade e confiança no Buda assim: ‘O
Abençoado é um arahant, perfeitamente iluminado, consumado no verdadeiro
conhecimento e conduta, bem-aventurado, conhecedor dos mundos, um líder
insuperável de pessoas preparadas para serem treinadas, mestre de devas e
humanos, desperto, sublime.’ Essa é a primeira fonte de mérito, fonte daquilo
que é benéfico, alimento da felicidade.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Outra vez,
bhikkhus, o nobre discípulo possui perfeita claridade, serenidade e confiança no Dhamma assim: ‘O
Dhamma é bem proclamado pelo Abençoado, visível no aqui e agora, com efeito
imediato, que convida ao exame, que conduz para adiante, para ser experimentado
pelos sábios por eles mesmos.’ Essa é a segunda fonte de mérito, fonte daquilo
que é benéfico, alimento da felicidade.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Outra vez,
bhikkhus, o nobre discípulo possui perfeita claridade, serenidade e confiança na Sangha assim:
‘Sangha dos discípulos do Abençoado pratica o bom caminho, pratica o caminho
reto, pratica o caminho verdadeiro, pratica o caminho adequado, isto é, os
quatro pares de pessoas, os oito tipos de indivíduos; esta Sangha dos
discípulos do Abençoado é merecedora de dádivas, merecedora de hospitalidade,
merecedora de oferendas, merecedora de saudações com reverência, um campo
inigualável de mérito para o mundo.’ Essa é a terceira fonte de mérito, fonte
daquilo que é benéfico, alimento da felicidade.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Outra vez,
bhikkhus, o nobre discípulo permanece em casa com uma mente desprovida da
mácula da avareza, espontaneamente generoso, mão aberta, que se delicia com a
renúncia, devotado à caridade, deliciando-se em dar e compartir. Essa é a
quarta fonte de mérito, fonte daquilo que é benéfico, alimento da felicidade.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Essas são
as quatro fontes de méritos, fontes daquilo que é benéfico, alimento da
felicidade.”<o:p></o:p></span></p>


