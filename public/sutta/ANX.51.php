<p class=Normal>Assim ouvi. Certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no Parque de Anathapindika. Lá ele se dirigiu aos monges da seguinte forma: “Bhikkhus!” “Sim, venerável senhor,” os monges responderam. O Abençoado disse o seguinte: </p>

<p class=Normal> “Bhikkhus, um bhikkhu que não é hábil nos modos das mentes dos outros deveria treinar: 'Eu terei habilidade nos modos da minha própria mente.' Assim vocês devem treinar. </p>

<p class=Normal> “Como um bhikkhu tem habilidade nos modos da sua própria mente? Tal como uma mulher, ou um homem, jovem, sadio, que aprecia ornamentos, ao ver a imagem do rosto refletida num espelho límpido e brilhante ou numa bacia com água cristalina, percebe uma mancha ou defeito, fará esforço para removê-la, mas não percebendo mancha ou defeito ficará feliz assim: ‘É um ganho para mim que esteja perfeito!’</p> 

<p class=Normal>"Do mesmo modo, para um bhikkhu a auto-análise é muito benéfica para o incremento de qualidades benéficas: ‘Eu com freqüência permaneço com cobiça ou não? Com pensamentos de má vontade ou não? Subjugado pela preguiça e torpor ou não? Com inquietação ou não? Com dúvida ou tendo superado a dúvida? Com raiva ou não? Com pensamentos contaminados ou livres de contaminação? Com o corpo excitado ou sem excitação? Com preguiça ou com a energia estimulada? Sem concentração ou concentrado?’ </p>

<p class=Normal> “Se através dessa auto-análise um bhikkhu sabe: ‘Eu com freqüência permaneço com cobiça, com pensamentos de má vontade, subjugado pela preguiça e torpor, com inquietação, com dúvida, com raiva, com pensamentos contaminados, com o corpo excitado, com preguiça ou sem concentração,’ então ele deve aplicar um desejo adicional, esforço, diligência, empenho, atenção plena e plena consciência para o abandono dessas mesmas qualidades ruins e prejudiciais. Tal como se o turbante ou o cabelo de uma pessoa estivesse em chamas ela empregaria desejo adicional, empenho, diligência, esforço, atenção plena e plena consciência para apagar o fogo do seu turbante ou cabelo, da mesma forma o bhikkhu deveria empregar desejo adicional, empenho, diligência, esforço, atenção plena e plena consciência para o abandono dessas mesmas qualidades ruins e prejudiciais.</p>

<p class=Normal> “Mas, se através dessa auto-análise um bhikkhu sabe: ‘Eu com freqüência permaneço sem cobiça, sem pensamentos de má vontade, livre da preguiça e torpor, livre da inquietação, sem dúvida, sem raiva, sem pensamentos contaminados, com o corpo sem excitação, com a energia estimulada, com concentração,’ então, ele deveria basear-se nessas mesmas qualidades benéficas e fazer um esforço adicional para realizar a destruição das impurezas.” </p> 

