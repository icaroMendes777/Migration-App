<p class=Normal> Assim ouvi. Em certa
ocasião o Abençoado estava em Savathi no Bosque de Jeta, no Parque de
Anathapindika. Então, quando a noite estava bem avançada, uma certa devata com
belíssima aparência que iluminou todo o Bosque de Jeta, se aproximou do
Abençoado. Ao se aproximar ela homenageou o Abençoado e ficando em pé a um lado
a devata disse: <o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'> Muitos devas e seres humanos<br>
desejando a felicidade,<br>
pensam na proteção.<br>
Diga-me então, qual é a proteção suprema. <o:p></o:p></span></p>


<p class=Normal><i>O Buda:</span></i>  <o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'> Não se associar com os tolos,<br>
associar-se com os sábios,<br>
demonstrar respeito àqueles dignos de respeito:<br>
&nbsp;&nbsp;&nbsp; Essa é a proteção suprema. <o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'> Viver num local adequado,[<a href="#N1">1</a>]<br>
<a name=R1></a>ter realizado méritos no passado,<br>
portar-se de maneira correta:[<a href="#N2">2</a>]<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<a name=R2></a> Essa é a proteção suprema. <o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'> Conhecimento amplo e habilidade,[<a href="#N3">3</a>]<br>
<a name=R3></a>bem treinado na disciplina,[<a href="#N4">4</a>]<br>
<a name=R4></a>linguagem proveitosa: [<a href="#N5">5</a>]<br>
&nbsp;&nbsp;&nbsp; <a name=R5></a>Essa é a proteção suprema. <o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'> Sustentar o pai e a mãe,<br>
zelar pela esposa e filhos,<br>
dedicar-se a uma ocupação pacífica:<br>
&nbsp;&nbsp;&nbsp; Essa é a proteção suprema. <o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'> Generosidade, conduta íntegra,[<a href="#N6">6</a>]<br>
<a name=R6></a>auxiliar os parentes,<br>
praticar ações que não sejam passíveis de censura:<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Essa é a proteção suprema. <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> Evitar e abster-se daquilo que é ruim e prejudicial;<br>
abster-se do que provoca embriaguez,<br>
estar atento às qualidades da mente:<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Essa é a proteção suprema. <o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'> Respeito e humildade,<br>
contentamento e gratidão,<br>
ouvir o Dhamma em ocasiões oportunas:<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Essa é a proteção suprema. <o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'> Paciência e obediência,<br>
visitar bhikkhus e bhikkhunis,<br>
discutir o Dhamma em ocasiões oportunas:<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Essa é a proteção suprema. <o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR>Autocontrole,
uma vida santa casta,<br>
compreender as Nobres Verdades,<br>
realizar Nibbana:<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Essa é a proteção suprema. </span></p>


<p class=Normal style='margin-left:.75in'> Uma mente que não é tocada<br>
pelas vicissitudes do mundo,[<a href="#N7">7</a>]<br>
<a name=R7></a>livre da tristeza, purificada das impurezas, segura:<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Essa é a proteção suprema. <o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'> Aqueles que assim se comportam,<br>
sempre invencíveis,<br>
estabelecidos na felicidade:<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Para eles essas são as proteções supremas.
