<p class=Normal><a name=N1></a><span lang=PT-BR>[1] Qualquer local no qual
residam monges, monjas e discípulos leigos; no qual pessoas devotas se dedicam
a praticar as dez ações meritórias, e onde o Dhamma exista como princípio de
vida. [<a href="#R1">Retorna</a>]</span></p>

<p class=Normal><a name=N2></a><span lang=PT-BR>[2] Tomar a decisão
correta de abandonar a não virtude em favor da virtude, a ausência de fé em
favor da fé e o egoísmo em favor da generosidade. [<a href="#R2">Retorna</a>]</span></p>


<p class=Normal><a name=N3></a><span lang=PT-BR>[3] Habilidade para
realizar as tarefas de um chefe de família ou de um monástico, sem causar dano
a ninguém e sem que nada de ruim seja feito. [<a href="#R3">Retorna</a>]</span></p>


<p class=Normal><a name=N4></a><span lang=PT-BR>[4] <i>Vinaya</i>
significa disciplina nos pensamentos, linguagem e ação. O comentário fala de
dois tipos de disciplina – do chefe de família, que compreende abster-se dos
dez tipos de ações ruins e prejudiciais (<i>akusala-kammapatha</i>), e dos
monges que é seguir as regras do<span style="mso-spacerun: yes"> 
</span>Patimokkha, o código de disciplina monástica. [<a href="#R4">Retorna</a>]</span></p>


<p class=Normal><a name=N5></a><span lang=PT-BR>[5] A linguagem proveitosa
é aquela que é oportuna, verdadeira, amistosa, benéfica e dita com pensamentos
de amor bondade. [<a href="#R5">Retorna</a>]</span></p>


<p class=Normal><a name=N6></a><span lang=PT-BR>[6] Conduta íntegra é
praticar as dez ações benéficas (<i>kusala-kammapatha</i>) com os pensamentos,
linguagem e ação: libertar a mente da cobiça, má vontade e entendimento
incorreto; evitar a linguagem mentirosa, maliciosa, grosseira e frívola; e não
matar, roubar, praticar atos sexuais <a name=PVW>inapropriados</a>. [<a
href="#R6">Retorna</a>]</span></p>


<p class=Normal><a name=N7></a><span lang=PT-BR>[7] As vicissitudes do
mundo são oito em número: ganho e perda, fama e má reputação, elogio e crítica,
alegria e tristeza. Este verso se refere ao estado mental de um arahant. [<a
href="#R7">Retorna</a>]</span></p>





