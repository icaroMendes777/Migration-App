<p class=Normal> “Bhikkhus, há essas cinco bases para a libertação, e quando um bhikkhu permanece diligente, ardente e decidido em qualquer uma delas, a sua mente que não está libertada se liberta, as suas impurezas que não foram destruídas são destruídas e ele realiza a ainda não realizada insuperável libertação do cativeiro. Quais cinco? </p>

<p class=Normal> “Aqui, bhikkhus, o Mestre, ou um respeitado bhikkhu na posição de um mestre, ensina o Dhamma para um bhikkhu. Mesmo enquanto o mestre lhe ensina o Dhamma, aquele bhikkhu experimenta o significado e o Dhamma. Com essa experiência surge nele a satisfação; dessa satisfação surge o êxtase; com o êxtase o corpo se acalma; com o corpo acalmado ele sente felicidade; como resultado, com essa felicidade, a mente se concentra. Essa é a primeira base para a libertação e quando um bhikkhu permanece nela diligente, ardente e decidido, a sua mente que não está libertada se liberta, as suas impurezas que não foram destruídas são destruídas e ele realiza a ainda não realizada insuperável libertação do cativeiro. </p>

<p class=Normal> “Além disso, bhikkhus, nem o Mestre e tampouco um respeitado bhikkhu na posição de um mestre, ensina o Dhamma para um bhikkhu. Mas o próprio bhikkhu ensina o Dhamma em detalhe para os outros, tal como ele o aprendeu e aperfeiçoou. Mesmo enquanto ele ensina o Dhamma, o bhikkhu experimenta o significado e o Dhamma. Com essa experiência surge nele a satisfação; dessa satisfação surge o êxtase; com o êxtase o corpo se acalma; com o corpo acalmado ele sente felicidade; como resultado, com essa felicidade, a mente se concentra. Essa é a segunda base para a libertação e quando um bhikkhu permanece nela diligente, ardente e decidido, a sua mente que não está libertada se liberta, as suas impurezas que não foram destruídas são destruídas e ele realiza a ainda não realizada insuperável libertação do cativeiro. </p>

<p class=Normal> “Além disso, bhikkhus, nem o Mestre e tampouco um respeitado bhikkhu na posição de um mestre, ensina o Dhamma para um bhikkhu; nem o próprio bhikkhu ensina o Dhamma em detalhe para os outros, tal como ele o aprendeu e aperfeiçoou. Mas ele recita o Dhamma em detalhe, tal como ele o aprendeu e aperfeiçoou. Mesmo enquanto ele recita o Dhamma, o bhikkhu experimenta o significado e o Dhamma. Com essa experiência surge nele a satisfação; dessa satisfação surge o êxtase; com o êxtase o corpo se acalma; com o corpo acalmado ele sente felicidade; como resultado, com essa felicidade, a mente se concentra. Essa é a terceira base para a libertação e quando um bhikkhu permanece nela diligente, ardente e decidido, a sua mente que não está libertada se liberta, as suas impurezas que não foram destruídas são destruídas e ele realiza a ainda não realizada a insuperável libertação do cativeiro. </p>

<p class=Normal> “Além disso, bhikkhus, nem o Mestre e tampouco um respeitado bhikkhu na posição de um mestre, ensina o Dhamma para um bhikkhu; nem o próprio bhikkhu ensina o Dhamma em detalhe para os outros, tal como ele o aprendeu e aperfeiçoou; nem ele recita o Dhamma em detalhe, tal como ele o aprendeu e aperfeiçoou. Mas ele pondera, examina e investiga mentalmente o Dhamma, tal como ele o aprendeu e aperfeiçoou. Mesmo enquanto ele pondera, examina e investiga mentalmente o Dhamma, o bhikkhu experimenta o significado e o Dhamma. Com essa experiência surge nele a satisfação; dessa satisfação surge o êxtase; com o êxtase o corpo se acalma; com o corpo acalmado ele sente felicidade; como resultado, com essa felicidade, a mente se concentra. Essa é a quarta base para a libertação e quando um bhikkhu permanece nela diligente, ardente e decidido, a sua mente que não está libertada se liberta, as suas impurezas que não foram destruídas são destruídas e ele realiza a ainda não realizada insuperável libertação do cativeiro. </p>

<p class=Normal> “Além disso, bhikkhus, nem o Mestre e tampouco um respeitado bhikkhu na posição de um mestre, ensina o Dhamma para um bhikkhu; nem o próprio bhikkhu ensina o Dhamma em detalhe para os outros, tal como ele o aprendeu e aperfeiçoou; nem ele recita o Dhamma em detalhe, tal como ele o aprendeu e aperfeiçoou; nem ele pondera, examina e investiga mentalmente o Dhamma, tal como ele o aprendeu e aperfeiçoou. Mas ele está bem familiarizado com algum sinal de concentração, [<a href="#N1">1</a>]<a name="R1"> </a>  considerou-o bem, investigou-o com a sua mente, e penetrou-o corretamente com a sabedoria. Mesmo enquanto ele aprende bem um certo sinal de concentração, o bhikkhu experimenta o significado e o Dhamma. Com essa experiência surge nele a satisfação; dessa satisfação surge o êxtase; com o êxtase o corpo se acalma; com o corpo acalmado ele sente felicidade; como resultado, com essa felicidade, a mente se concentra. Essa é a quinta base para a libertação e quando um bhikkhu permanece nela diligente, ardente e decidido, a sua mente que não está libertada se liberta, as suas impurezas que não foram destruídas são destruídas e ele realiza a ainda não realizada insuperável libertação do cativeiro. </p>

<p class=Normal> “Bhikkhus, essas são as cinco bases para a libertação, e quando um bhikkhu permanece diligente, ardente e decidido em qualquer uma delas, a sua mente que não está libertada se liberta, as suas impurezas que não foram destruídas são destruídas e ele realiza a ainda não realizada insuperável libertação do cativeiro.” </p>

