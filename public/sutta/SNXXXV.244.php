<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
quando um bhikkhu sabe como na verdade é a origem e a cessação de todos e quaisquer
estados que envolvam o sofrimento, então, os prazeres sensuais foram vistos por
ele de tal modo que ao vê-los, o desejo sensual, a afeição sensual, a obsessão
sensual, a paixão sensual relacionada aos prazeres sensuais, não se encontram
latentes nele; então, ele compreendeu um modo de conduta e 
permanência de tal maneira que ao se conduzir desse modo e permanecer desse
modo, estados ruins e prejudiciais de cobiça e tristeza não o invadem. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como,
bhikkhus, um bhikkhu sabe como na verdade é a origem e a cessação de todos e
quaisquer estados que envolvam o sofrimento? ‘Assim é a forma, essa é a sua
origem, essa é a sua cessação; assim é a sensação ... assim é a percepção ...
assim são as formações ... assim é a consciência, essa é a sua origem, essa é a
sua cessação’: é desse modo que um bhikkhu sabe como na verdade é a origem e a
cessação de todos e quaisquer estados que envolvam o sofrimento.<span
style="mso-spacerun: yes">  </span><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como,
bhikkhus, os prazeres sensuais foram vistos por ele de tal maneira que ao vê-los,
o desejo sensual, a afeição sensual, a obsessão sensual, a paixão sensual
relacionada aos prazeres sensuais, não se encontram latentes nele? </span><span
lang=PT-BR style='mso-bidi-font-size:10.0pt;mso-ansi-language:PT-BR'>Suponham
que<a name=pit> houves</a>se uma cova com carvão em brasa, mais profunda que a
altura de um homem, repleta de carvão em brasas sem arder e fumegar, [<a
href="#N1">1</a>] <a name=R1></a>e surgisse um homem </span>que valoriza a vida
e teme a morte, que deseja o prazer e abomina a dor<span lang=PT-BR
style='mso-bidi-font-size:10.0pt;mso-ansi-language:PT-BR'>. Então dois homens
fortes, agarrando-o pelos braços, o arrastassem até a cova cheia de carvão em
brasas. O homem contorceria o seu corpo nesta e naquela direção. Por que isso?
Porque ele sabe que: ‘Eu cairei nessa cova cheia de carvão em brasas e assim
morrerei ou sofrerei dores mortíferas.’ Da mesma forma,</span><span
lang=PT-BR style='mso-ansi-language:PT-BR'> bhikkhus, quando um bhikkhu viu os
prazeres sensuais como semelhantes a uma cova com carvão em brasa, o desejo
sensual, a afeição sensual, a obsessão sensual, a paixão sensual relacionada
aos prazeres sensuais, não se encontram latentes nele.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como,
bhikkhus, ele compreendeu um modo de conduta e  permanência de tal
maneira que ao se conduzir desse modo e permanecer desse modo, estados ruins e
prejudiciais de cobiça e tristeza não o invadem? Suponham que um homem entrasse
numa floresta espinhosa. Haveria espinhos na frente dele, espinhos atrás dele,
espinhos à sua esquerda, espinhos à sua direita, espinhos abaixo dele, espinhos
acima dele. Ele iria para frente com atenção plena, ele iria para trás com
atenção plena, pensando, ‘Que os espinhos não me piquem!’ Da mesma forma,
bhikkhus, qualquer coisa no mundo que tenha uma natureza prazerosa e agradável
é chamada de espinho na Disciplina dos Nobres. Ao compreender isso dessa
maneira, como ‘um espinho,’ ele deve entender a contenção e a não contenção. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como,
bhikkhus, ocorre a não contenção? Neste caso, ao ver uma<span style='mso-bidi-font-weight:
bold'> forma com o olho, um bhikkhu se empenha pela forma prazerosa e repele a
forma des-prazerosa. Ele permanece sem estabelecer a atenção plena no corpo,
com a mente limitada, e não compreende como na verdade é a libertação da mente,
a libertação através da sabedoria, na qual aqueles estados ruins e prejudiciais
cessam sem deixar vestígio. Ao ouvir um som com o ouvido ... Ao conscientizar um
objeto mental com a mente, ele se empenha pelo objeto mental prazeroso e repele
o objeto mental des-prazeroso. Elea permanece sem estabelecer a atenção plena
no corpo, com a mente limitada, e não compreende como na verdade é a libertação
da mente, a libertação através da sabedoria, na qual aqueles estados ruins e
prejudiciais cessam sem deixar vestígio. É desse modo que ocorre a não
contenção. <o:p></o:p></span></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como,
bhikkhus, ocorre a contenção? Neste caso, ao ver uma<span style='mso-bidi-font-weight:
bold'> forma com o olho, um bhikkhu não se empenha pela forma prazerosa e não
repele a forma des-prazerosa. Ele permanece com a atenção plena no corpo
estabelecida, com a mente ilimitada, e compreende como na verdade é a
libertação da mente, a libertação através da sabedoria, na qual aqueles estados ruins
e prejudiciais cessam sem deixar vestígio. Ao ouvir um som com o ouvido ... Ao
conscientizar um objeto mental com a mente, ele não se empenha pelo objeto mental
prazeroso e não<span style="mso-spacerun: yes">  </span>repele o objeto mental
des-prazeroso. Ele permanece com a atenção plena no corpo estabelecida, com a
mente ilimitada, e compreende como na verdade é a libertação da mente, a
libertação através da sabedoria, na qual aqueles estados ruins e prejudiciais cessam
sem deixar vestígio. É desse modo que ocorre a contenção</span>.<span
style='mso-bidi-font-weight:bold'><o:p></o:p></span></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando,
bhikkhus, um bhikkhu tem essa conduta e permanência, se alguma vez, devido
a um lapso na atenção plena, memórias e intenções ruins e prejudiciais
conectadas com os grilhões surgirem nele, a atenção plena dele pode ser lenta
no seu surgimento, mas ele rapidamente abandona, remove, elimina e aniquila
aquilo. Como se um homem deixasse cair duas ou três gotas d'água
sobre uma chapa de ferro aquecida durante todo um dia, a queda das gotas
poderia ser lenta, mas elas se vaporizariam rapidamente e desapareceriam ao tocar a chapa de ferro. Da
mesma forma, quando bhikkhu tem essa conduta e permanência ... a atenção
plena dele pode ser lenta no seu surgimento, mas ele rapidamente abandona,
remove, elimina e aniquila aquilo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Assim um
bhikkhu compreendeu um modo de conduta e  permanência de tal maneira
que ao se conduzir desse modo e permanecer desse modo, estados ruins e
prejudiciais de cobiça e tristeza não o invadem.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando um
bhikkhu tem essa conduta e permanência, pode ser que reis ou ministros
reais, amigos ou colegas, parentes ou pares, o convidem a aceitar riquezas,
dizendo: ‘Venha, bom homem, por que permitir que esses mantos de cor ocre o
sobrecarreguem? Por que perambular com a cabeça raspada e uma tigela para
esmolar? Venha, ao regressar para a vida comum, desfrute da riqueza e de
ações meritórias.’ De fato, bhikkhus, quando aquele bhikkhu tem essa conduta e
permanência, é impossível que ele abandone o treinamento e regresse para a
vida comum. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Suponham,
bhikkhus, que onde o rio Gânges decliva, tende e se inclina na direção leste,
uma grande multidão de pessoas viesse com pás e cestos, pensando: ‘Faremos com
que este rio Gânges declive, tenda e se incline na direção oeste.’ O que vocês
pensam bhikkhus, aquela multidão de pessoas seria capaz de fazer com que o rio
Gânges declive, tenda e se incline na direção oeste?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
venerável senhor. Por qual razão? Porque o rio Gânges decliva, tende e se
inclina na direção leste e não é fácil fazer com que ele declive, tenda e se
incline na direção oeste. Aquela grande multidão de pessoas iria colher apenas
cansaço e aflição.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Da mesma maneira,
bhikkhus, quando um bhikkhu tem essa conduta e permanência, pode ser que
reis ou ministros reais, amigos ou colegas, parentes ou pares o convidem a
aceitar riquezas ... [mas] é impossível que ele abandone o treinamento e
regresse para a vida comum. Por qual razão? Porque por muito tempo a mente
dele tem declivado, tendido e inclinado na direção do isolamento. Portanto,
é impossível<span style="mso-spacerun: yes">  </span>que ele abandone o
treinamento e regresse para a vida comum.”<o:p></o:p></span></p>

