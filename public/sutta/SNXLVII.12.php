<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em certa
ocasião o Abençoado estava<span style="mso-spacerun: yes">  </span>em Nalanda
no mangueiral de Pavarika. Então, o Venerável Sariputta foi até o Abençoado e
depois de cumprimentá-lo sentou a um lado e disse:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, eu tenho tamanha confiança no Abençoado que acredito que não existe ou
nunca existirá no presente um outro contemplativo ou brâmane com mais
conhecimento do que o Abençoado com respeito à iluminação.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sublime de
fato é essa sua afirmação bramada, Sariputta, você rugiu o definitivo e
categórico rugido do leão: [<a href="#N1">1</a>] <a name=R1></a>‘Venerável
senhor, eu tenho tamanha confiança no Abençoado que acredito que não existe ou
nunca existirá no presente um outro contemplativo ou brâmane com mais
conhecimento do que o Abençoado com respeito à iluminação.’ Você agora,
Sariputta, compreendeu com a sua mente as mentes de todos os Arahants, os
Perfeitamente Iluminados, que surgiram no passado e assim compreendeu: ‘Esses
Abençoados tinham tal virtude ou tais qualidades, ou tal sabedoria, ou tal
permanência, ou tal libertação’?” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
venerável senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
Sariputta, você compreendeu com a sua mente as mentes de todos os Arahants, os
Perfeitamente Iluminados, que surgirão no futuro e assim compreendeu: ‘Esses
Abençoados terão tal virtude ou tais qualidades, ou tal sabedoria, ou tal
permanência, ou tal libertação’?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
venerável senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
Sariputta, você compreendeu com a sua mente a minha própria mente – eu sendo no
momento o Arahant, o Perfeitamente Iluminado - e assim compreendeu: ‘O
Abençoado tem tal virtude ou tais qualidades, ou tal sabedoria, ou tal
permanência, ou tal libertação’?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
venerável senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sariputta,
se você não tem o conhecimento compreendendo as mentes dos Arahants, os
Perfeitamente Iluminados do passado, do futuro e do presente, porque você faz
essa sublime afirmação bramada e ruge o definitivo e categórico rugido do leão:
‘Venerável senhor, eu tenho tamanha confiança no Abençoado que acredito que não
existe ou nunca existirá no presente um outro contemplativo ou brâmane com mais
conhecimento do que o Abençoado com respeito à iluminação.’?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu não
tenho, venerável senhor o conhecimento compreendendo as mentes dos Arahants, os
Perfeitamente Iluminados do passado, do futuro e do presente, mas ainda assim
compreendi isso através da inferência do Dhamma. Suponha, venerável senhor, que
um rei tivesse uma cidade fronteiriça com sólidas muralhas, proteções e abóbadas
e com um único portão. O guardião ali postado seria sábio, competente e
inteligente; alguém que não permite a entrada de desconhecidos e admite que
entrem os conhecidos. Enquanto ele patrulha seguindo o caminho que circunda a
cidade ele não vê uma fissura ou abertura nas muralhas grande o suficiente
mesmo para permitir que um gato passe. Ele poderia pensar: ‘Quaisquer criaturas
com bom tamanho que entrem ou saiam desta cidade, todas entram e saem através
deste portão.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Do mesmo
modo, venerável senhor, eu compreendi isso através da inferência do Dhamma:
Todos os Arahants, Perfeitamente Iluminados, que surgiram no passado, todos
esses Abençoados primeiro abandonaram esses cinco obstáculos, corrupções da
mente e enfraquecedores da sabedoria; e depois, com as suas mentes bem
estabelecidas nos quatro fundamentos da atenção plena, eles desenvolveram
corretamente os sete fatores da iluminação; e dessa forma eles despertaram para
a insuperável perfeita iluminação. E, venerável senhor, todos os Arahants,
Perfeitamente Iluminados, que irão surgir no futuro, todos esses Abençoados
primeiro irão abandonar esses cinco obstáculos, corrupções da mente e
enfraquecedores da sabedoria; e depois, com as suas mentes bem estabelecidas
nos quatro fundamentos da atenção plena, eles irão desenvolver corretamente os
sete fatores da iluminação; e dessa forma eles irão despertar para a
insuperável perfeita iluminação. E, venerável senhor, o Abençoado, que é no
presente o Arahant, o Perfeitamente Iluminado, primeiro abandonou esses cinco
obstáculos, corrupções da mente e enfraquecedores da sabedoria; e depois, com a
sua mente bem estabelecida nos quatro fundamentos da atenção plena,<span
style="mso-spacerun: yes">  </span>desenvolveu corretamente os sete fatores da
iluminação; e dessa forma despertou para a insuperável perfeita
iluminação.&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Muito bem,
Sariputta! Portanto, Sariputta, você deve repetir com freqüência esta
explicação do Dhamma para os bhikkhus e bhikkhunis, para os discípulos leigos
homens e mulheres. Muito embora algumas pessoas tolas tenham dúvidas ou
incertezas em relação ao Tathagata, ao ouvirem esta explicação do Dhamma,<span
style="mso-spacerun: yes">  </span>a dúvida e a incerteza delas em relação ao
Tathagata serão abandonadas.” <o:p></o:p></span></p>


