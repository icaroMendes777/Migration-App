<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim ouvi.
Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no Parque de
Anathapindika.<o:p></o:p></span></p>



<p class=Normal><a name=T2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2. Então, enquanto o venerável Malunkyaputta estava só em meditação, o
seguinte pensamento lhe ocorreu:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Estas
idéias especulativas não foram declaradas pelo Abençoado, foram deixadas de
lado e rejeitadas por ele, isto é: ‘o mundo é eterno’ ou ‘o mundo não é eterno’;
‘o mundo é finito’ ou ‘o mundo é infinito’; ‘a alma e o corpo são a mesma coisa’
ou ‘a alma é uma coisa e o corpo é outra’; ‘após a morte um Tathagata existe’ ou
‘após a morte um Tathagata não existe’; ‘após a morte um Tathagata tanto existe
como não existe’ ou ‘após a morte um Tathagata nem existe, nem não existe.’ O
Abençoado não declara essas coisas para mim, e eu não aprovo e não aceito o
fato dele não declarar essas coisas para mim, portanto irei até o Abençoado e
perguntarei o significado disso. Se ele declarar para mim que ‘o mundo é
eterno’ ou ‘o mundo não é eterno’...‘após a morte um Tathagata nem existe, nem
não existe,’ então eu viverei a vida santa sob a orientação dele; se ele não
declarar essas coisas para mim, então abandonarei o treinamento e retornarei
para a vida comum.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. Então,
quando anoiteceu, o venerável Malunkyaputta levantou da meditação e foi até o
Abençoado. Depois de cumprimentá-lo ele sentou a um lado e lhe disse:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
venerável senhor, enquanto eu estava só em meditação, o seguinte pensamento me
ocorreu: ‘Estas idéias especulativas não foram declaradas pelo Abençoado...se
ele não declarar essas coisas para mim, então abandonarei o treinamento e
retornarei para a vida comum.’ Se o Abençoado sabe que ‘o mundo é eterno,’ que
o Abençoado declare para mim que ‘o mundo é eterno’; Se o Abençoado sabe que ‘o
mundo não é eterno,’ que o Abençoado declare para mim que ‘o mundo não é
eterno.’ Se o Abençoado não sabe que ‘o mundo é eterno’ ou que ‘o mundo não é
eterno,’ então é justo que quem não sabe e não vê diga: ‘Eu não sei, eu não
vejo.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se o
Abençoado sabe que ‘o mundo é finito,’...‘o mundo é infinito,’...‘a alma e o
corpo são a mesma coisa,’...‘a alma é uma coisa e o corpo é outra,’...‘após a
morte um Tathagata existe,’...‘após a morte um Tathagata não existe,’ ... se o
Abençoado sabe que ‘após a morte um Tathagata tanto existe como não existe,’ que o
Abençoado declare isso para mim; se o Abençoado sabe que ‘após a morte um
Tathagata nem existe, nem não existe,’ que o Abençoado declare isso para
mim.<span style="mso-spacerun: yes">  </span>Se o Abençoado não sabe que ‘após
a morte um Tathagata existe e não existe’ ou que ‘após a morte um Tathagata nem
existe, nem não existe,’ então é justo que quem não sabe e não vê diga: ‘Eu não
sei, eu não vejo.’ <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. “Como
então, Malunkyaputta, em algum momento eu lhe disse: ‘Venha, Malunkyaputta,
viva a vida santa sob a minha orientação que eu irei declarar para você que ‘o
mundo é eterno’...ou ‘após a morte um Tathagata nem existe, nem não existe’?” –
“Não, venerável senhor.” – “Você em algum momento me disse: ‘Eu viverei a vida
santa sob a orientação do Abençoado e o Abençoado irá declarar para mim ‘o
mundo é eterno’...ou ‘após a morte um Tathagata nem existe, nem não existe’?” –
“Não, venerável senhor.” – “Em sendo assim, homem tolo, quem é você e o que
você está abandonando?<o:p></o:p></span></p>



<p class=Normal><a name=T5></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>5. “Se alguém dissesse que: ‘Eu não viverei a vida santa sob a
orientação do Abençoado até que o Abençoado declare para mim que “o mundo é
eterno”...ou “após a morte um Tathagata nem existe, nem não existe,” essas
coisas ainda permaneceriam não declaradas e nesse meio tempo aquela pessoa
teria morrido. Suponha, Malunkyaputta, que um homem fosse ferido com uma flecha
besuntada com muito veneno e os seus amigos e companheiros, seus pares e
parentes, trouxessem um cirurgião para tratá-lo. O homem diria: ‘Eu não
permitirei que o cirurgião retire a flecha até que eu saiba se o homem que me
feriu era um nobre ou um brâmane ou um comerciante ou um trabalhador.’ E ele
diria: ‘Eu não permitirei que o cirurgião retire a flecha até que eu saiba o
nome e o clã do homem que me feriu;...até que eu saiba se o homem que me feriu
era alto ou baixo ou com estatura mediana;...até que eu saiba se o homem que me
feriu tinha a tez escura ou marrom ou dourada;...até que eu saiba se o homem
que me feriu vive em tal vilarejo ou vila, ou cidade;...até que eu saiba se o
arco que me feriu era um arco longo ou balestra;...até que eu saiba se a corda
do arco que me feriu era de fibra ou junco, ou tendão, ou cânhamo, ou casca de
árvore;...até que eu saiba se a vara da flecha que me feriu era selvagem ou
cultivada;...até que eu saiba que tipo de penas traz a flecha que me feriu – de
uma águia ou um corvo, ou um falcão, ou um pavão, ou uma cegonha;...até que eu
saiba que tipo de tendão amarra a flecha que me feriu – de um boi ou um búfalo,
ou um leão, ou um macaco;...até que eu saiba que tipo de flecha me feriu – com
a ponta em forma de casco ou em curva, ou com farpas, ou com dentes de um
bezerro, ou com o veneno da adelfa.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Tudo isso
ainda não seria do conhecimento daquele homem e nesse meio tempo ele teria
morrido. Assim também, Malunkyaputta, se alguém dissesse que: ‘Eu não viverei a
vida santa sob a orientação do Abençoado até que o Abençoado declare para mim
que “o mundo é eterno”...ou “após a morte um Tathagata nem existe, nem não
existe,” essas coisas ainda permaneceriam não declaradas e nesse meio tempo
aquela pessoa teria morrido.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. “Malunkyaputta,
se existe a idéia ‘o mundo é eterno,’ a vida santa não pode ser vivida; e se
existe a idéia ‘o mundo não é eterno,’ a vida santa não pode ser vivida.
Existindo ou não a idéia ‘o mundo é eterno’ ou a idéia ‘o mundo não é eterno,’
existe nascimento, existe envelhecimento, existe morte, existe tristeza,
lamentação, dor, angústia e desespero, a destruição dos quais eu prescrevo aqui
e agora.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se existe
a idéia ‘o mundo é finito,’...‘o mundo é infinito’...‘a alma e o corpo são a
mesma coisa’...‘a alma é uma coisa e o corpo é outra’...‘após a morte um
Tathagata existe’...‘após a morte um Tathagata não existe,’ a vida santa não
pode ser vivida...se existe a idéia ‘após a morte um Tathagata tanto existe como
não existe,’ a vida santa não pode ser vivida; e se existe a idéia ‘após a
morte um Tathagata nem existe, nem não existe,’ a vida santa não pode ser
vivida. Existindo ou não a idéia ‘após a morte um Tathagata tanto existe como não
existe’ ou a idéia ‘após a morte um Tathagata nem existe, nem não existe,’ existe<span
style="mso-spacerun: yes">  </span>nascimento, existe envelhecimento, existe
morte, existe tristeza, lamentação, dor, angústia e desespero, a destruição dos
quais eu prescrevo aqui e agora.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7.
“Portanto, Malunkyaputta, lembre-se daquilo que eu deixei sem declarar como não
declarado e lembre-se daquilo que eu declarei como declarado. E o que eu deixei
sem declarar? ‘O mundo é eterno’ – eu deixei sem declarar. ‘O mundo não é
eterno’– eu deixei sem declarar. ‘O mundo é finito’ – eu deixei sem declarar.
‘O mundo é infinito’– eu deixei sem declarar. ‘a alma e o corpo são a mesma
coisa’ – eu deixei sem declarar. ‘A alma é uma coisa e o corpo é outra’– eu
deixei sem declarar. ‘Após a morte um Tathagata existe’ – eu deixei sem
declarar. ‘Após a morte um Tathagata não existe’ – eu deixei sem declarar.
‘Após a morte um Tathagata tanto existe como não existe’ – eu deixei sem declarar.
‘Após a morte um Tathagata nem existe, nem não existe’– eu deixei sem declarar.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. Porque
eu não declararei isso? Porque não traz beneficio, não pertence aos fundamentos
da vida santa, não conduz ao desencantamento, ao desapego, à cessação, à paz,
ao conhecimento direto, à iluminação, a Nibbana. É por isso que eu não declarei
isso.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. E o que
eu declarei? ‘Isto é sofrimento’ – eu declarei. ‘Esta é a origem do sofrimento’
– eu declarei. ‘Esta é a cessação do sofrimento’ – eu declarei. ‘Este é o
caminho que conduz à cessação do sofrimento’ – eu declarei.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. “Porque
declarei isso? Porque traz benefício, pertence aos fundamentos da vida santa,
conduz ao desencantamento, ao desapego, à cessação, à paz, ao conhecimento
direto, à iluminação, a Nibbana. É por isso que eu declarei isso.<o:p></o:p></span></p>



<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>11. “Portanto, Malunkyaputta, lembre-se daquilo que eu deixei sem
declarar como não declarado e lembre-se daquilo que eu declarei como
declarado.”<o:p></o:p></span></p>



<p class=Normal><a name=R31></a><a name=R1></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>Isso foi o que disse o Abençoado. O venerável
Malunkyaputta ficou satisfeito e contente com as palavras do Abençoado. [<a
href="#N1">1</a>]<o:p></o:p></span></p>


