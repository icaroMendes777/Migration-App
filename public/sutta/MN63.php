<p class=Tit1 align=center style='text-align:center'>Culamalunkya Sutta </Tit1>
<p class=Tit1 align=center style='text-align:center'>O Pequeno Discurso para Malunkyaputta </Tit1>

<p class=Normal align=center style='text-align:center'><span lang=PT-BR style='font-size:
