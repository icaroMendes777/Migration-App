<p class=Normal> (22-29) (982) "Bhikkhus, para o conhecimento direto da cobiça, onze coisas devem ser desenvolvidas. Quais onze? (1) O primeiro jhana, (2) o segundo jhana, (3) o terceiro jhana, (4) o quarto jhana, (5) a libertação da mente através do amor bondade, (6) a libertação da mente através da compaixão, (7) a libertação da mente através da alegria altruísta, (8) a libertação da mente através da equanimidade, (9) a base do espaço infinito, (10) a base da consciência infinita. e (11) a base do nada. Para o conhecimento direto da cobiça, essas onze coisas devem ser desenvolvidas." </p>

