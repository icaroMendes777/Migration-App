<p class=Normal> Em Savatthi. “Bhikkhus, eu digo que a destruição das impurezas é
realizada por aquele que sabe e que vê, não por aquele que não sabe e que não
vê. Aquele que sabe o que e aquele que vê o que, realiza a destruição das
impurezas? ‘Assim é a forma, essa é a sua origem, essa é a sua cessação. Assim
é a sensação, essa é a sua origem, essa é a sua cessação. Assim é a percepção,
essa é a sua origem, essa é a sua cessação. Assim são as formações volitivas, essa é a
sua origem, essa é a sua cessação. Assim é a consciência, essa é a sua origem,
essa é a sua cessação.’ A destruição das impurezas é realizada por aquele que
sabe dessa forma e que vê dessa forma.<o:p></o:p></span></p>


<p class=Normal> “Mesmo que um bhikkhu tenha este desejo, ‘Que a minha mente se liberte
das impurezas através da ausência de apego!’ mas se ele permanecer sem se
dedicar à prática, ainda assim sua mente não ficará liberta das impurezas através
da ausência de apego. Por que ocorre isso? Por falta de prática, deve ser dito.
Por falta de praticar o que? Os quatro fundamentos da atenção plena, os quatro
esforços corretos, as quatro bases de poder, as cinco faculdades, os cinco
poderes, os sete fatores da iluminação, o nobre caminho óctuplo. <o:p></o:p></span></p>


<p class=Normal> “Suponham que uma galinha tenha oito, dez ou doze ovos. Se ela não
cobrí-los corretamente, aquecê-los corretamente ou incubá-los corretamente,
então mesmo que ela tenha o desejo, ‘Que as minhas crias rompam as cascas dos
ovos com suas garras afiadas ou bicos e saiam dos ovos com segurança!’ ainda
assim não será possível que os pintos rompam as cascas dos ovos com suas garras
afiadas ou bicos e saiam dos ovos com segurança. Por que ocorre isto? Porque a
galinha não cobriu-os corretamente, aqueceu-os corretamente, incubou-os corretamente. Da mesma maneira, mesmo que este desejo ocorra a um bhikkhu que
permanece sem se dedicar à prática, ‘Que a minha mente se liberte das impurezas
através da ausência de apego!’ ainda assim sua mente não estará liberta das
impurezas através da ausência de apego. Por que ocorre isso? Por falta de
prática, deve ser dito. Por falta de praticar o que? Os quatro fundamentos da
atenção plena, os quatro esforços corretos, as quatro bases de poder, as cinco
faculdades, os cinco poderes, os sete fatores da iluminação, o nobre caminho
óctuplo. <o:p></o:p></span></p>


<p class=Normal> “Mesmo que um bhikkhu não tenha este desejo, ‘Que a minha mente se
liberte das<span style="mso-spacerun: yes">  </span>impurezas através da
ausência de apego!’ mas se ele permanecer dedicado à prática, ainda assim a sua
mente será libertada das impurezas através da ausência de apego. Por que ocorre
isso? Pela prática, deve ser dito. Por praticar o que? Os quatro fundamentos da
atenção plena, os quatro esforços corretos, as quatro bases de poder, as cinco
faculdades, os cinco poderes, os sete fatores da iluminação, o nobre caminho
óctuplo. <o:p></o:p></span></p>


<p class=Normal> “Suponham que uma galinha tenha oito, dez ou doze ovos que ela cobriu
corretamente, aqueceu corretamente ou incubou corretamente. Mesmo que este desejo
não lhe ocorra, ‘Que as minhas crias rompam as cascas dos ovos com suas garras
afiadas ou bicos e saiam dos ovos com segurança!’ ainda assim será possível que
os pintos rompam as cascas dos ovos com suas garras afiadas ou bicos e saiam
dos ovos com segurança. Por que ocorre isto? Porque a galinha cobriu-os
corretamente, aqueceu-os corretamente, incubou-os corretamente. Da mesma
maneira, mesmo que este desejo não ocorra a um bhikkhu que permanece se
dedicando à prática, ‘Que a minha mente se liberte das impurezas através da
ausência de apego!’, ainda assim a sua mente será libertada das impurezas
através da ausência de apego. Por que ocorre isso? Pela prática, deve ser dito.
Por praticar o que? Os quatro fundamentos da atenção plena, os quatro esforços
corretos, as quatro bases de poder, as cinco faculdades, os cinco poderes, os
sete fatores de iluminação, o nobre caminho óctuplo. <o:p></o:p></span></p>


<p class=Normal> “Do mesmo modo que um carpinteiro ou aprendiz de carpinteiro vê as
marcas de seus dedos, ou do dedão, no cabo do seu enxó, embora sem saber, ‘Hoje
o cabo do meu enxó foi gasto este tanto, ou ontem ele foi gasto outro tanto, ou
anteontem ele foi gasto outro tanto’, quando estiver gasto ele saberá que está
gasto. Assim também, quando um bhikkhu permanece dedicado à prática, embora sem
saber, ‘Hoje minhas impurezas gastaram este tanto, ou ontem elas gastaram um
outro tanto, ou anteontem elas gastaram um outro tanto,’ quando elas estiverem
gastas ele saberá que estão gastas. <o:p></o:p></span></p>


<p class=Normal> “Suponham bhikkhus, que um navio transoceânico, equipado com mastros e
cordame, desgastado após seis meses na água, fosse levado para uma doca seca
durante o período do inverno, o seu cordame exposto ao calor e ao vento,
umedecido pelas nuvens da estação das chuvas, facilmente encolheria e
apodreceria. Assim também, quando um bhikkhu permanece dedicado à prática, seus
grilhões facilmente encolhem e apodrecem.” <o:p></o:p></span></p>


