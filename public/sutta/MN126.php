<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim ouvi.
Em certa ocasião o Abençoado estava em Rajagaha, no Bambual, no Santuário dos
Esquilos.<o:p></o:p></span></p>


<p class=Normal><a
name=T2></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. Então, </span>ao
amanhecer<span lang=PT-BR style='mso-ansi-language:PT-BR'>, o venerável Bhumija
se vestiu e tomando a tigela e o manto externo foi até a casa do Príncipe
Jayasena e sentou num assento que havia sido preparado. [<a href="#N1">1</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>3. Então, o Príncipe Jayasena foi até o venerável Bhumija e ambos se
cumprimentaram. Quando a conversa cortês e amigável havia terminado, ele sentou
a um lado e disse para o venerável Bhumija: “Mestre Bhumija, existem alguns
contemplativos e brâmanes que fazem estas afirmações e possuem estas idéias:
‘Se alguém fizer uma aspiração [<a href="#N2">2</a>] <a name=R2></a>e viver a
vida santa, será incapaz de obter qualquer fruto; se alguém não fizer nenhuma
aspiração e viver a vida santa, ainda assim será incapaz de obter qualquer
fruto; se alguém fizer e não fizer uma aspiração e viver a vida santa, ainda
assim será incapaz de obter qualquer fruto; se alguém nem fizer uma aspiração,
nem não fizer uma aspiração e viver a vida santa, ainda assim será incapaz de
obter qualquer fruto.’ O que o venerável mestre Bhumija diz disso, o que ele
declara?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. “Eu não
ouvi, nem aprendi<span style="mso-spacerun: yes">  </span>isso da boca do
Abençoado, príncipe. Mas é possível que o Abençoado dissesse isto: ‘Se alguém
fizer uma aspiração e viver a vida santa sem sabedoria, será incapaz de obter
qualquer fruto; se alguém não fizer nenhuma aspiração e viver a vida santa sem
sabedoria, ainda assim será incapaz de obter qualquer fruto; se alguém fizer e
não fizer uma aspiração e viver a vida santa sem sabedoria, ainda assim será
incapaz de obter qualquer fruto; se alguém nem fizer uma aspiração, nem não
fizer uma aspiração e viver a vida santa sem sabedoria, ainda assim será
incapaz de obter qualquer fruto. No entanto, se alguém fizer uma aspiração e
viver a vida santa com sabedoria, será capaz de obter algum fruto; se alguém
não fizer nenhuma aspiração e viver a vida santa com sabedoria, ainda assim
será capaz de obter algum fruto; se alguém fizer e não fizer uma aspiração e
viver a vida santa com sabedoria, ainda assim será capaz de obter algum fruto;
se alguém nem fizer uma aspiração, nem não fizer uma aspiração e viver a vida
santa com sabedoria, ainda assim será capaz de obter algum fruto.’ Eu não ouvi,
nem aprendi isso da boca do Abençoado, mas é possível que o Abençoado
respondesse da forma como eu declarei.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. “Se o
professor do mestre Bhumija afirma isso, se ele declara isso, então com certeza
parece que o mestre do venerável Bhumija se destaca de todos os contemplativos
e brâmanes comuns.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. Então o
Príncipe Jayasena serviu o venerável Bhumija da sua própria tigela de arroz
doce.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. Depois,
quando o venerável Bhumija retornou da sua ronda de esmolar alimentos, após a
refeição, ele foi até o Abençoado. Após cumprimentá-lo, ele sentou a um lado e
relatou o que havia ocorrido, adicionando: “Venerável senhor, eu espero que ao
ter sido perguntado tal questão e respondido dessa forma, eu tenha dito aquilo
que foi dito pelo Abençoado e que eu não o tenha deturpado com algo contrário
aos fatos. Eu espero que tenha explicado de acordo com o Dhamma, de tal modo
que nada que dê margem à censura possa de forma legítima ser deduzido da minha
declaração.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. “Com
certeza, Bhumija, ao ter sido perguntado tal questão e respondido dessa forma,
você disse aquilo que foi dito por mim e não me deturpou com algo contrário aos
fatos. Você explicou de acordo com o Dhamma de tal modo que nada que dê margem
à censura possa de forma legítima ser deduzido da sua declaração.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9.
“Quaisquer contemplativos e brâmanes, que possuam entendimento incorreto, pensamento
incorreto, linguagem incorreta, ação incorreta, modo de vida incorreto, esforço
incorreto, atenção plena incorreta e concentração incorreta, se eles fizerem
uma aspiração e viverem a vida santa, serão incapazes de obter qualquer fruto;
se eles não fizerem nenhuma aspiração e viverem a vida santa, ainda assim serão
incapazes de obter qualquer fruto; se eles fizerem e não fizerem uma aspiração
e viverem a vida santa, ainda assim serão incapazes de obter qualquer fruto; se
eles nem fizerem uma aspiração, nem não fizerem uma aspiração e viverem a vida
santa, ainda assim serão incapazes de obter qualquer fruto. Por que isso? Porque
esse (caminho incorreto) não é um método adequado para obter frutos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10.
“Suponham um homem que precisasse de óleo, estivesse buscando óleo,
perambulasse procurando por óleo, colocasse cascalho num balde, borrifasse o
cascalho com água e o espremesse. Então, se ele fizesse uma aspiração e agisse de
acordo, ele seria incapaz de obter qualquer óleo; se ele não fizesse uma
aspiração e agisse de acordo, ainda assim ele seria incapaz de obter qualquer
óleo; se ele fizesse e não fizesse uma aspiração e agisse de acordo, ainda
assim ele seria incapaz de obter qualquer óleo; se ele nem fizesse uma
aspiração e nem não fizesse uma aspiração e agisse de acordo, ainda assim ele
seria incapaz de obter qualquer óleo. Por que isso? Porque essa (forma de agir)
não é um método adequado para obter óleo. Do mesmo modo, quaisquer
contemplativos e brâmanes que possuam entendimento incorreto ... ainda assim
serão incapazes de obter qualquer fruto. Por que isso? Porque esse (caminho
incorreto) não é um método adequado para obter frutos. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11.
“Suponham um homem que precisasse de leite, estivesse buscando leite,
perambulasse procurando por leite, agarrasse os chifres de uma vaca. Então, se
ele fizesse uma aspiração ... se ele não fizesse uma aspiração ... se ele
fizesse e não fizesse uma aspiração ... se ele nem fizesse uma aspiração e nem
não fizesse uma aspiração e agisse de acordo, ainda assim ele seria incapaz de
obter qualquer leite. Por que isso? Porque essa (forma de agir) não é um método
adequado para obter leite. Do mesmo modo, quaisquer contemplativos e brâmanes
que possuam entendimento incorreto ... ainda assim serão incapazes de obter
qualquer fruto. Por que isso? Porque esse (caminho incorreto) não é um método
adequado para obter frutos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style="mso-spacerun: yes"> </span>12. “Suponham um homem que precisasse de
manteiga, estivesse buscando manteiga, perambulasse procurando por manteiga,
despejasse água em uma desnatadeira e a desnatasse com um pau. Então, se ele
fizesse uma aspiração ... se ele não fizesse uma aspiração ... se ele
fizesse<span style="mso-spacerun: yes">  </span>e não fizesse uma aspiração ...
se ele nem fizesse uma aspiração e nem não fizesse uma aspiração e agisse de
acordo, ainda assim ele seria incapaz de obter qualquer manteiga. Por que isso?
Porque essa (forma de agir) não é um método adequado para obter manteiga. Do
mesmo modo, quaisquer contemplativos e brâmanes que possuam entendimento
incorreto ... ainda assim serão incapazes de obter qualquer fruto. Por que isso?
Porque esse (caminho incorreto) não é um método adequado para obter frutos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13.
“Suponham um homem que precisasse de fogo, estivesse buscando fogo, perambulasse
procurando por fogo, esfregasse dois pedaços de madeira verde molhados. Então,
se ele fizesse uma aspiração ... se ele não fizesse uma aspiração ... se ele
fizesse e não fizesse uma aspiração ... se ele nem fizesse uma aspiração e nem
não fizesse uma aspiração e agisse de acordo, ainda assim ele seria incapaz de
obter qualquer fogo. Por que isso? Porque essa (forma de agir) não é um método
adequado para obter fogo. Do mesmo modo, quaisquer contemplativos e brâmanes
que possuam entendimento incorreto ... ainda assim serão incapazes de obter
qualquer fruto. Por que isso? Porque esse (caminho incorreto) não é um método
adequado para obter frutos. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14.
“Quaisquer contemplativos e brâmanes que possuam entendimento correto, pensamento
correto, linguagem correta, ação correta, modo de vida correto, esforço
correto, atenção plena correta e concentração correta, se eles fizerem uma
aspiração e viverem a vida santa, serão capazes de obter algum fruto; se eles
não fizerem nenhuma aspiração e viverem a vida santa, ainda assim serão capazes
de obter algum fruto; se eles fizerem e não fizerem uma aspiração e viverem a
vida santa, ainda assim serão capazes de obter algum fruto; se eles nem fizerem
uma aspiração, nem não fizerem uma aspiração e viverem a vida santa, ainda
assim serão capazes de obter algum fruto. Por que isso? Porque esse (caminho
correto) é um método adequado para obter frutos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15.
“Suponham um homem que precisasse de óleo, estivesse buscando óleo,
perambulasse procurando por óleo, colocasse sementes de gergelim em um balde,
borrifasse o gergelim com água e o espremesse. Então, se ele fizesse uma
aspiração e agisse de acordo, ele seria capaz de obter algum óleo; se ele não
fizesse uma aspiração e agisse de acordo, ainda assim ele seria capaz de obter
algum óleo; se ele fizesse e não fizesse uma aspiração e agisse de acordo,
ainda assim ele seria capaz de obter algum óleo; se ele nem fizesse uma
aspiração e nem não fizesse uma aspiração e agisse de acordo, ainda assim ele
seria capaz de obter algum óleo. Por que isso? Porque essa (forma de agir) é um
método adequado para obter óleo. Do mesmo modo, quaisquer contemplativos e
brâmanes que possuam entendimento correto ... ainda assim serão capazes de
obter algum fruto. Por que isso? Porque esse (caminho correto) é um método
adequado para obter frutos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16.
“Suponham um homem que precisasse de leite, estivesse buscando leite,
perambulasse procurando por leite, agarrasse o ubre de uma vaca. Então, se ele
fizesse uma aspiração ... se ele não fizesse uma aspiração ... se ele fizesse e
não fizesse uma aspiração ... se ele nem fizesse uma aspiração e nem não
fizesse uma aspiração e agisse de acordo, ainda assim ele seria capaz de obter
algum leite. Por que isso? Porque essa (forma de agir) é um método adequado para
obter leite. Do mesmo modo, quaisquer contemplativos e brâmanes que possuam
entendimento correto ... ainda assim serão capazes de obter algum fruto. Por que
isso? Porque esse (caminho correto) é um método adequado para obter frutos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17.
“Suponham um homem que precisasse de manteiga, estivesse buscando manteiga,
perambulasse procurando por manteiga, despejasse leite em uma desnatadeira e o
desnatasse com um pau. Então, se ele fizesse uma aspiração ... se ele não
fizesse uma aspiração ... se ele fizesse e não fizesse uma aspiração ... se ele
nem fizesse uma aspiração e nem não fizesse uma aspiração e agisse de acordo,
ainda assim ele seria capaz de obter alguma manteiga. Por que isso? Porque essa
(forma de agir) é um método adequado para obter manteiga. Do mesmo modo,
quaisquer contemplativos e brâmanes que possuam entendimento correto ... ainda
assim serão capazes de obter algum fruto. Por que isso? Porque esse (caminho
correto) é um método adequado para obter frutos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18.
“Suponham um homem que precisasse de fogo, estivesse buscando fogo,
perambulasse procurando por fogo, esfregasse dois pedaços de madeira secos.
Então, se ele fizesse uma aspiração ... se ele não fizesse uma aspiração ... se
ele fizesse e não fizesse uma aspiração ... se ele nem fizesse uma aspiração e
nem não fizesse uma aspiração e agisse de acordo, ainda assim ele seria capaz
de obter algum fogo. Por que isso? Porque essa (forma de agir) é um método
adequado para obter fogo. Da mesma forma, quaisquer contemplativos e brâmanes
que possuam entendimento correto ... ainda assim serão capazes de obter algum
fruto. Por que isso? Porque esse (caminho correto) é um método adequado para
obter frutos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>19.
“Bhumija, se esses quatro símiles lhe tivessem ocorrido com (relação a) o
Príncipe Jayasena, ele teria espontaneamente adquirido confiança em você, e
tendo confiança ele teria lhe mostrado confiança.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, como poderiam esses quatro símiles ter ocorrido a mim com (relação a) o
Príncipe Jayasena da forma como eles ocorrem para o Abençoado, visto que eles
são espontâneos e nunca foram ouvidos antes?”<o:p></o:p></span></p>



<p class=Normal><a name=R31></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Isso foi o que disse o Abençoado. O venerável Bhumija ficou satisfeito e
contente com as palavras do Abençoado. <o:p></o:p></span></p>


