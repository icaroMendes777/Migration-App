<p class=Normal> Então, o venerável Phagguna foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse: “Venerável senhor, há algum olho através do qual alguém descrevendo os Budas do passado poderia descrevê-los - aqueles que realizaram o parinibbana, eliminaram a proliferação, eliminaram o caminho, deram um fim ao ciclo e superaram todo sofrimento? Há algum ouvido através do qual alguém descrevendo os Budas do passado poderia descrevê-los? ... Há alguma mente através da qual alguém descrevendo os Budas do passado poderia descrevê-los - aqueles que realizaram o parinibbana, eliminaram a proliferação, eliminaram o caminho, deram um fim ao ciclo e superaram todo sofrimento?"</p>

<p class=Normal> "Não há olho, Phagguna, através do qual alguém descrevendo os Budas do passado poderia descrevê-los - aqueles que realizaram o parinibbana, eliminaram a proliferação, eliminaram o caminho, deram um fim ao ciclo e superaram todo sofrimento. Não há ouvido através do qual alguém descrevendo os Budas do passado poderia descrevê-los ... Não há mente através da qual alguém descrevendo os Budas do passado poderia descrevê-los - aqueles que realizaram o parinibbana, eliminaram a proliferação, eliminaram o caminho, deram um fim ao ciclo e superaram todo sofrimento."</p>

