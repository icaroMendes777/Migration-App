que é a faculdade da atenção plena é o poder da atenção plena; aquilo que é o
poder da atenção plena é a faculdade da atenção plena. Aquilo que é a faculdade
da concentração é o poder da concentração; aquilo que é o poder da concentração
é a faculdade da concentração. Aquilo que é a faculdade da sabedoria é o poder
da sabedoria; aquilo que é o poder da sabedoria é a faculdade da sabedoria.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Suponham,
bhikkhus, que há um rio que declive, tenda, se incline na direção leste, com uma
