<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] Essa afirmação indica que não há uma diferença substancial entre as
faculdades e os poderes, que ambos compreendem os mesmos cinco fatores vistos
por dois ângulos distintos. Embora seja tentador enxergar os poderes, <i>(bala),
</i>como um estágio mais avançado no desenvolvimento das faculdades, não há
nada no cânone, ou nos comentários, que suporte essa idéia. O comentário diz
que um fator é a faculdade da convicção “no sentido de exercer o controle sobre
a característica da decisão”, e o poder da convicção “no sentido de não ser
abalado pela falta de convicção”. De modo semelhante, as outras quatro
faculdades exercem respectivamente controle sobre a aplicação, estabelecimento,
não distração e visão; elas são poderes no sentido de não serem abaladas pela
preguiça, esquecimento, distração e ignorância. [<a href="#R1">Retorna</a>]<o:p></o:p></span></p>






