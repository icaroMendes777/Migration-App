considerado um rio com uma correnteza, mas há um método de exposição através do
qual aquele rio poderia ser considerado um rio com duas correntezas.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual é o
método de exposição através do qual aquele rio poderia ser considerado um rio
com uma correnteza? Tomando em conta a água ao leste da ilha e a água a oeste
da ilha – esse é o método de exposição através do qual aquele rio poderia ser
considerado um rio com uma correnteza.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual é o
método de exposição através do qual aquele rio poderia ser considerado um rio
