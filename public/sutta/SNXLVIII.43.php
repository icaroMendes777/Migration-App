<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Há um
método de exposição, bhikkhus, através do qual as cinco faculdades se convertem
nos cinco poderes e os cinco poderes se convertem nas cinco faculdades. E qual
é esse método de exposição? Aquilo que é a faculdade da convicção é o poder da
convicção; aquilo que é o poder da convicção é a faculdade da convicção. [<a
href="#N1">1</a>] <a name=R1></a>Aquilo que é a faculdade da energia é o poder
