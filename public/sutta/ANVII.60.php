<p class=Normal style='margin-left:.75in'>



<p class=Normal>
&quot;Essas sete coisas - que agradam a um inimigo, que concretizam o
objetivo de um inimigo - sucedem a um homem ou mulher que são enraivecidos. Quais
sete? <o:p></o:p></span></p>

<p class=Normal>
&quot;Há o caso em que alguém deseja para o seu inimigo, 'Que essa
pessoa seja feia!' Por que ocorre isso? Porque ela não fica satisfeita com a
boa aparência de um inimigo. Agora quando uma pessoa é enraivecida - tomada pela
raiva, oprimida pela raiva - então apesar do fato de que ela possa estar banhada,
bem untada, vestida com roupas brancas, seu cabelo e barba aparados, apesar
disso ela é feia, tudo porque ela está tomada pela raiva. Essa é a primeira
coisa que agrada a um inimigo, que concretiza o objetivo de um inimigo, que
sucede a um homem ou mulher que são enraivecidos. <o:p></o:p></span></p>


<p class=Normal>
&quot;Além disso, alguém deseja para o seu inimigo, 'Que essa pessoa
durma mal!' Por que ocorre isso? Porque ela não fica satisfeita com o sono
reparador de um inimigo. Agora, quando uma pessoa é enraivecida - tomada pela raiva,
oprimida pela raiva - então apesar do fato de ela dormir em uma cama coberta com colchas felpudas, coberta com colchas de lã branca, colchas
bordadas, peles de antílope e gamo, coberta com um baldaquino e com almofadas
vermelhas para a cabeça e os pés, apesar disso ela dorme mal,
tudo porque ela está tomada pela raiva. Essa é a segunda coisa que agrada a um
inimigo, que concretiza o objetivo de um inimigo, que sucede a um homem ou
mulher que são enraivecidos. <o:p></o:p></span></p>

<p class=Normal>
&quot;Além disso, alguém deseja para o seu inimigo, 'Que essa pessoa
não tenha ganhos!' Por que ocorre isso? Porque ela não fica satisfeita com os
ganhos de um inimigo. Agora quando uma pessoa é enraivecida - tomada pela raiva,
oprimida pela raiva - então mesmo quando ela sofre uma perda, ela pensa, 'Eu
tive um ganho'; e mesmo quando ela tenha um ganho, ela pensa, ‘Eu tive uma
perda.' Quando ela se apropriou dessas idéias que operam em mútua oposição [em
relação à verdade], elas conduzem<span style="mso-spacerun: yes">  </span>ao
seu sofrimento e perda por um longo prazo, tudo porque ela está tomada pela
raiva. Essa é a terceira coisa que agrada a um inimigo, que concretiza o
objetivo de um inimigo, que sucede a um homem ou mulher que são enraivecidos <o:p></o:p></span></p>

<p class=Normal>
&quot;Além disso, alguém deseja para o seu inimigo, 'Que essa pessoa
não tenha nenhuma fortuna!&quot; Por que ocorre isso? Porque ela não fica
satisfeita com a fortuna de um inimigo. Agora, quando uma pessoa é enraivecida -
tomada pela raiva, oprimida pela raiva - então qualquer fortuna que ela
tenha, adquirida através do seu esforço e empreendimento, acumulada pela força
do seu braço, acumulada com o suor do seu rosto - fortuna justa ganha
corretamente - o rei ordena que ela seja enviada ao tesouro real [como
pagamento de multas cobradas por seu comportamento] tudo porque ela está tomada
pela raiva. Essa é a quarta coisa que agrada a um inimigo, que concretiza o
objetivo de um inimigo, que sucede a um homem ou mulher que são enraivecidos <o:p></o:p></span></p>


<p class=Normal>
&quot;Além disso, alguém deseja para o seu inimigo, 'Que essa pessoa
não seja honrada!' Por que ocorre isso? Porque ela não fica satisfeita que um
inimigo seja honrado. Agora, quando uma pessoa é enraivecida - tomada pela raiva,
oprimida pela raiva - qualquer honra que ela tenha ganho por ser atenciosa,
esta é perdida, tudo porque ela esta tomada pela raiva. Essa é a quinta coisa
que agrada a um inimigo, que concretiza o objetivo de um inimigo, que sucede a
um homem ou mulher que são enraivecidos <o:p></o:p></span></p>

<p class=Normal>
&quot;Além disso, alguém deseja para o seu inimigo, 'Que essa pessoa
não tenha amigos!' Por que ocorre isso? Porque ela não fica satisfeita quando
um inimigo tem amigos. Agora, quando uma pessoa é enraivecida - tomada pela
raiva, oprimida pela raiva - seus amigos, companheiros e parentes a evitarão
de longe, tudo porque ela está tomada pela raiva. Essa é a sexta coisa que
agrada a um inimigo, que concretiza o objetivo de um inimigo, que sucede a um
homem ou mulher que são enraivecidos <o:p></o:p></span></p>

<p class=Normal>
&quot;Além disso, alguém deseja para o seu inimigo, 'Que essa pessoa,
na dissolução do corpo após a morte, renasça num estado de privação, num destino infeliz, nos
reinos inferiores, até mesmo no <span style='mso-bidi-font-weight:bold'>inferno!' Por que ocorre
isso? Porque ela não fica satisfeita quando um inimigo vai para o paraíso.
Agora, quando uma pessoa é enraivecida - tomada pela raiva, oprimida pela raiva
- ela se comporta de forma inábil com o  corpo, com a linguagem, com a mente.
Tendo se comportado de forma inábil com o corpo, com a linguagem, com a mente,
então - na dissolução do corpo, após a morte - ela renasce num estado de privação, num destino
infeliz, nos reinos inferiores, até mesmo no inferno, tudo porque ela está tomada pela raiva. Essa é a sétima coisa que
agrada a um inimigo, que concretiza o objetivo de um inimigo, que sucede a um
homem ou mulher que são enraivecidos </span><span lang=PT-BR style='font-size:
12.0pt'><o:p></o:p></span></p>


<p class=Normal>
&quot;Essas são as sete coisas - que agradam a um inimigo, que
concretizam o objetivo de um inimigo - que sucedem a um homem ou mulher que são
enraivecidos.&quot; <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR>Uma pessoa colérica é feia e dorme mal.<br>
Tendo um ganho, ela o converte em uma perda,<br>
causa danos com a linguagem e com atos.<br>
Uma pessoa cheia de raiva<br>
destrói sua fortuna.<br>
Enlouquecida pela raiva,<br>
ela destrói o seu status.<br>
Parentes, amigos e colegas a evitam.<br>
A raiva resulta em perda.<br>
A raiva inflama a mente.<br>
Ela não se dá conta<br>
que o perigo nasce de dentro.<br>
Uma pessoa enraivecida não conhece o seu próprio benefício.<br>
Uma pessoa enraivecida não enxerga o Dhamma.<br>
Uma pessoa conquistada pela raiva é uma massa de escuridão.<br>
Ela tem prazer em atos perversos como se eles fossem bons,<br>
porém mais tarde, quando a raiva passa,<br>
ela sofre como se fosse queimada com fogo.<br>
Ela é corrompida, maculada,<br>
tal como o fogo envolto na fumaça. </span></p>

<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR>Quando a raiva se espalha,<br>
quando uma pessoa fica enraivecida,<br>
ela não tem vergonha, não teme o mal,<br>
não tem a linguagem respeitosa.<br>
Para uma pessoa tomada pela raiva,<br>
nada proporciona luz. </span></p>

<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR>Eu mencionarei os atos que trazem remorso,<br>
que não fazem parte dos ensinamentos.<br>
Ouçam!<br>
Uma pessoa enraivecida mata o seu pai,<br>
mata sua mãe,<br>
mata Brâmanes<br>
e pessoas comuns.<br>
É por causa da devoção de uma mãe<br>
que a pessoa vê o mundo,<br>
no entanto uma pessoa colérica<br>
pode matar a quem lhe dá a vida.<br>
Todos os seres têm grande estima por si mesmos,<br>
no entanto uma pessoa colérica, desorientada,<br>
pode matar a si mesma de várias formas:<br>
com uma espada, tomando veneno,<br>
enforcando-se com uma corda. </span></p>

<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR>Realizando essas ações<br>
que matam outros seres e violentam a si mesmo,<br>
a pessoa colérica não se dá conta de que está arruinada. </span></p>

<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR>Essa armadilha de Mara, sob a forma da
raiva,<br>
habitando na caverna do coração:<br>
sem autocontrole,<br>
discernimento, persistência, entendimento correto.<br>
O homem sábio daria um basta <br>
em toda e qualquer falta de habilidade.<br>
Treinem assim:<br>
'Que nós não sejamos maculados.’</span></p>

<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR>Livre da raiva e despreocupado,<br>
livre do desejo, desapegado,<br>
amansado, abandonada a raiva,<br>
livre de impurezas,<br>
você estará libertado.</span></p>

