<p class=Tit1 align=center style='text-align:center'>Kodhana Sutta </Tit1>
<p class=Tit1 align=center style='text-align:center'>Uma Pessoa Enraivecida </Tit1>

<p class=Normal align=center style='text-align:center'><span lang=PT-BR style='font-size:
