<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E quais,
bhikkhus, são os estados que conduzem à iluminação? A faculdade da convicção,
bhikkhus, é um estado que conduz à iluminação; leva à iluminação. A faculdade
da energia é um estado que conduz à iluminação; leva à iluminação. A faculdade
da atenção plena é um estado que conduz à iluminação; leva à iluminação. A
faculdade da concentração é um estado que conduz à iluminação; leva à
iluminação. A faculdade da sabedoria é um estado que conduz à iluminação; leva
à iluminação.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Asim como,
