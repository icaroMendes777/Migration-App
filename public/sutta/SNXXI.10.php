<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR style='mso-ansi-language:
PT-BR'>“O sábio, conquistador de tudo, que tudo sabe, <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>imaculado dentre todas as coisas, que abandonou tudo,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>libertado com a destruição do desejo:<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>eu chamo essa pessoa ‘aquele que vive sozinho.’”<b style='mso-bidi-font-weight:
