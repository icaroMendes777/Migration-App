<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] As impurezas <i>(asava), </i>são uma categoria de contaminações que
existem no plano mais profundo e básico e que sustentam o ciclo samsárico. Os
comentários derivam a palavra a partir da raiz <i>su </i>que significa “fluir”.
Existe divergência entre os estudiosos sobre se o fluxo implícito no prefixo <i>a
</i>é para fora ou para dentro; por conseguinte, alguns o interpretam como
“fluxo para dentro” ou “influências”, outros como “fluxo para fora” ou
“efluentes”. Um trecho encontrado com freqüência nos suttas indica no entanto o
real significado do termo, independentemente da sua etimologia, quando descreve
os <i>asavas </i>como estados “que contaminam, que causam a renovação de
ser/existir, que causam problemas, que amadurecem no sofrimento e conduzem ao
futuro nascimento, envelhecimento e morte” (<a href="MN36.php#T47">MN 36.47</a>,
etc). Dessa forma outros tradutores, deixando de lado o sentido literal,
utilizam a interpretação de “máculas”, “corrupções” ou “impurezas” sendo que
esta última foi a escolha do Ven. Nanamoli. As três impurezas mencionadas nos
suttas são virtualmente sinônimas de desejo por prazeres sensuais, de desejo
por ser/existir e da ignorância que aparece no topo da fórmula da origem
dependente. MA explica que o controle <i>(samvara) </i>ocorre de cinco formas:
através da virtude, atenção plena, conhecimento, energia e paciência. Neste
sutta o controle através da virtude é ilustrado pelo evitar sentar-se em
assentos inadequados (verso19); o controle através da atenção plena, pela contenção
das faculdades dos sentidos (verso12); o controle através do conhecimento, pela
repetição da frase “refletindo de maneira sábia”; o controle através da
energia, pela remoção de pensamentos prejudiciais (verso 20); e o controle
através da paciência, pela passagem sobre o agüentar (verso18). [<a href="#R1">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[2] Atenção
com sabedoria <i>(yoniso manasikara) </i>é descrita como a atenção com os meios
corretos <i>(upaya) </i>e na direção correta <i>(patha).</i> É explicada como
diligência, consideração ou preocupação mental que está de acordo com a
verdade, isto é, atenção para com aquilo que é impermanente como impermanente,
etc. Atenção sem sabedoria <i>(ayoniso manasikara) </i>é a atenção com os meios
incorretos e na direção incorreta <i>(uppatha)</i>, contrária à verdade, isto
é, atenção para aquilo que é impermanente como permanente, o doloroso como
prazeroso, aquilo que é não-eu como sendo eu e o que é feio como bonito. MA nos
informa que a atenção sem sabedoria se encontra na raiz do ciclo de existências
pois ela faz com que o desejo e a ignorância aumentem; a atenção com sabedoria
se encontra na raiz da libertação do ciclo de existências pois ela conduz ao
desenvolvimento do Nobre Caminho Óctuplo. MA resume esta passagem do sutta
desta forma: a destruição das impurezas é feita por aquele que <i>sabe </i>como
estimular a atenção com sabedoria e que <i>cuida para</i> <i>que</i> a atenção
sem sabedoria não surja. [<a href="#R2">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N3></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[3] A
palavra “visão” <i>(dassana)</i> neste caso se refere ao primeiro dos quatro
caminhos supramundanos – o caminho de quem entrou na correnteza <i>(sotapattimagga)
</i>– descrito dessa forma porque proporciona o primeiro vislumbre de Nibbana.
Os três caminhos superiores são denominados os caminhos do desenvolvimento <i>(bhavana)</i>
porque eles desenvolvem a visão de Nibbana até o ponto em que todas as
impurezas são erradicadas. [<a href="#R3">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N4></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[4] O ponto
importante destacado por MA é de que não existe uma determinação fixa nas
coisas, por si mesmas, que as façam merecer ou não merecer a atenção. A
distinção consiste no modo da atenção. Aquele modo da atenção que é a causa
básica para estados prejudiciais da mente deve ser evitado, enquanto que aquele
modo da atenção que é a causa básica para estados benéficos da mente deve ser
desenvolvido. O mesmo princípio se aplica ao verso 9. [<a href="#R4">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N5></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[5] MA
ilustra o aumento das impurezas através da atenção sem sabedoria da seguinte
forma: quando ela se ocupa com a gratificação dos cinco prazeres sensuais, a impureza
do prazer sensual surge e aumenta; quando ela se ocupa com a gratificação dos
estados transcendentes (os jhanas), a impureza de ser/existir surge e aumenta;
e quando ela se ocupa com as coisas mundanas através das quatro “distorções”
(veja o <a
href="ANIV.49.php">Vipallasa Sutta - AN IV.49</a>), a impureza da ignorância surge e aumenta. [<a href="#R5">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N6></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[6] De
acordo com MA esta passagem tem como objetivo mostrar a impureza das idéias (<i>ditthasava,</i>
não expressamente mencionado no discurso) sob a perspectiva da dúvida. No
entanto, é provavelmente mais correto dizer que as impurezas das idéias,
reveladas no verso 8, surgem sob a forma de dúvida através da atenção sem
sabedoria. Os vários tipos de dúvida<span style="mso-spacerun: yes"> 
</span>estão plenos de entendimento incorreto que será claramente expressado na
seção seguinte. Veja também o <a
href="SNXII.20.php">Paccaya Sutta - SN XII.20</a>.[<a href="#R6">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N7></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[7] Dessas
seis idéias, as duas primeiras representam a antinomia simples entre o que é
eterno e a aniquilação; a idéia de que “não
existe um eu em mim” <i>não é</i> a doutrina de não-eu do Buda mas a visão
materialista que identifica o indivíduo com o corpo e dessa forma defende que
não existe continuidade da pessoa depois da morte. As três idéias seguintes</span><span
lang=PT-BR style='mso-ansi-language:PT-BR'> podem ser interpretadas como
surgindo da observação filosófica mais sofisticada de que as experiências
possuem como parte intrínseca uma estrutura reflexa que possibilita a
autoconsciência, a capacidade da mente de ter conhecimento acerca de si mesma,
do seu conteúdo e do corpo com o qual está inter-conectada. Engajada na busca
da sua “verdadeira natureza” a pessoa comum sem instrução irá identificar o eu
com ambos aspectos da experiência (idéia 3), ou somente com o observador (idéia
4) ou somente com aquilo que é observado (idéia 5). A última idéia é uma versão
completa do que é eterno, em que todas as reservas foram desprezadas. [<a
href="#R7">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N8></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[8] O eu,
sendo aquele que está discursando, representa a concepção do eu como agente da
ação; o eu como aquele que sente, a concepção do eu como agente passivo. “Aqui
e ali” sugere que o eu <span style="mso-spacerun: yes"> </span>é uma identidade
que transmigra retendo a sua identidade através de uma sucessão de diferentes
encarnações. [<a href="#R8">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N9></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[9] Esta é
a formulação das Quatro Nobres Verdades, tratadas como objeto de meditação e
insight. MA diz que até atingir o caminho de ‘entrar na correnteza’, a atenção
denota insight <i>(vipassana),</i> mas que no momento do caminho ela denota o
conhecimento do caminho. Com o insight existe a compreensão direta das duas
primeiras verdades já que a sua abrangência são os fenômenos mentais e
materiais compreendidos sob <i>dukkha</i> e a sua origem; só é possível
conhecer as últimas duas verdades por inferência. O conhecimento do caminho faz
da verdade da cessação do sofrimento o seu objeto, compreendendo-a através da
sua penetração como objeto <i>(arammana).</i> O conhecimento do caminho<i> </i>realiza
quatro funções em relação às quatro verdades: a verdade do sofrimento é
completamente compreendida, a origem do sofrimento é abandonada, a cessação do
sofrimento é realizada, o caminho que conduz à cessação do sofrimento é
desenvolvido. [<a href="#R9">Retorna</a>] <o:p></o:p></span></p>


<p class=Normal><a name=N10></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[10] O
caminho de ‘entrar na correnteza’ tem a função de cortar os primeiros três
grilhões que aprisionam ao samsara. MA diz que a idéia da existência de um eu e
o apego a preceitos e rituais, tendo sido incluídos nas impurezas das idéias,
são tanto impurezas como grilhões, enquanto que a dúvida é (normalmente)
classificada somente como um grilhão, não uma impureza; mas como está aqui
incluída entre as “impurezas que devem ser abandonadas através da visão,” pode
ser tratada como uma impureza. [<a href="#R10">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N11></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[11] Se o
abandono das impurezas é entendido no seu sentido estrito, ou seja, como a sua
completa destruição, então somente dois dos sete métodos mencionados no sutta
têm como resultado o abandono – visão e desenvolvimento – que entre si compreendem
os quatro caminhos supramundanos. Os outros cinco métodos não são capazes de
diretamente destruir as impurezas, mas eles podem mantê-las sob controle
durante os estágios preparatórios da prática e dessa forma facilitar a sua erradicação
pelos caminhos supramundanos. [<a href="#R11">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N12></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[12] O
principal fator responsável por exercer<span style="mso-spacerun: yes"> 
</span>controle sobre as faculdades sensoriais é a atenção plena. MA explica a
“febre” <i>(parilaha) </i>como a febre das impurezas e das suas
conseqüências (kamma). [<a href="#R12">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N13></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[13] Os
trechos que seguem se transformaram nas fórmulas padrão que os bhikkhus usam
nas suas reflexões diárias acerca dos requisitos da vida santa. [<a href="#R13">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N14></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[14]
Existem dois tipos de assentos inadequados mencionados no Patimokkha –
sentar-se com uma mulher em um local com uma tela de proteção que seja
conveniente para a prática sexual e sentar-se só com uma mulher em um local
privado. [<a href="#R14">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N15></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[15] Os primeiros três tipos de pensamentos prejudiciais – desejo
sensual, má vontade e crueldade – constituem pensamento incorreto que são o
oposto do segundo fator do Nobre Caminho Óctuplo. [<a href="#R15">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N16></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[16] Estes
são os sete fatores da iluminação <i>(satta bojjhanga) </i>incluídos entre os
trinta e sete apoios para a iluminação e tratados de maneira mais extensa no <a
href="MN10.php#T42">MN 10.42</a> e <a href="MN118.php#T29">MN 118.29-40</a>.
Esta seção explica os sete fatores da iluminação especificamente como
auxiliares no desenvolvimento dos três caminhos supramundanos superiores através
dos quais as impurezas que não foram erradicadas pelo primeiro caminho, sejam
erradicadas. Os termos “afastamento” <i>(viveka), </i>“desapego” <i>(viraga) </i>e
“cessação” <i>(nirodha)</i> podem ser todos interpretados como referindo-se a
Nibbana. O seu uso neste contexto significa que o desenvolvimento dos fatores
da iluminação está direcionado a Nibbana, como o seu objetivo durante os
estágios preparatórios do caminho e como seu objeto, ao atingir os caminhos
supramundanos. MA explica que a palavra <i>vossaga</i> interpretada como “abandono”
tem dois significados de “abrir mão” <i>(pariccaga),</i> isto é, o abandono das
impurezas e “entrar em” <i>(pakkhandana),</i> isto é, culminando em Nibbana. [<a
href="#R16">Retorna</a>]<o:p></o:p></span></p>


