impureza do desejo sensual é erradicada através do caminho do “não retorno”, as
impurezas de ser/existir e da ignorância somente são erradicadas com o caminho
final, o do arahant. [<a href="#R17">Retorna</a>]<o:p></o:p></span></p>


<p class=Normal><a name=N18></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[18] A
