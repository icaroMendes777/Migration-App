<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim ouvi.
Em certa ocasião o Abençoado estava em Savathi, no Bosque de Jeta, no Parque de
Anathapindika. Lá ele se dirigiu aos monges desta forma: “Bhikkhus”. –
“Venerável Senhor”, eles responderam. O Abençoado disse o seguinte”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.
“Bhikkhus, eu discursarei sobre o controle de todas as impurezas. [<a href="#N1">1</a>]
<a name=R1></a>Ouçam e prestem muita atenção àquilo que eu vou dizer.” – “Sim,
venerável senhor,” os bhikkhus responderam. O Abençoado disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><a name="T3"></a><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(Resumo)</b><span
style="mso-spacerun: yes">  </span><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. “Bhikkhus, eu digo que a
destruição das impurezas é realizada por aquele que sabe e que vê, não por
aquele que não sabe e que não vê. Aquele que sabe o que e aquele que vê o que,
realiza a destruição das impurezas? Atenção com sabedoria e atenção sem
sabedoria.[<a href="#N2">2</a>] <a name=R2></a>Quando alguém aplica a sua
atenção sem sabedoria, as impurezas que ainda não surgiram, surgem e as
impurezas que já surgiram, aumentam. Quando alguém aplica a sua atenção com
sabedoria, as impurezas que ainda não surgiram, não surgem e as impurezas que já
surgiram, são abandonadas.<o:p></o:p></span></p>

<p class=Normal><a name=T4></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>4.
“Bhikkhus, existem impurezas que devem ser abandonadas através da visão.
Existem impurezas que devem ser abandonadas através do autocontrole. Existem
impurezas que devem ser abandonadas pelo uso. Existem impurezas que devem ser
abandonadas com a paciência. Existem impurezas que devem ser abandonadas evitando.
Existem impurezas que devem ser abandonadas pela remoção. Existem impurezas que
devem ser abandonadas com a meditação.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(Impurezas que devem ser
abandonadas pela visão)</b><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. “Quais impurezas
bhikkhus, devem ser abandonadas pela visão? [<a href="#N3">3</a>] <a name=R3></a>Neste
caso bhikkhus, uma pessoa comum sem instrução que não respeita os nobres, que
não é proficiente nem treinada no Dhamma deles, que não respeita os homens
verdadeiros, que não é proficiente nem treinada no Dhamma deles, não
entende o tipo de coisas que merecem atenção e que tipo de coisas não merecem
atenção. Assim sendo, ela se ocupa com aquelas coisas que não merecem
atenção e não se ocupa com as coisas que merecem atenção. [<a href="#N4">4</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>6. “Quais são as coisas que não merecem atenção e com as quais ela se
ocupa?<span style="mso-spacerun: yes">  </span>São coisas tais que quando ela
se ocupa com elas, a impureza do desejo sensual que ainda não surgiu, surge
nela e a impureza do desejo sensual que já surgiu, aumenta nela; a impureza de ser/existir
que ainda não surgiu, surge nela e a impureza de ser/existir que já surgiu,
aumenta nela; a impureza da ignorância que ainda não surgiu, surge nela e a
impureza da ignorância que já surgiu, aumenta nela. Essas são as coisas que não
merecem atenção e com as quais ela se ocupa. [<a href="#N5">5</a>] <a name=R5></a>E
quais são as coisas que merecem atenção mas com as quais ela não se ocupa? São
coisas tais que quando ela se ocupa com elas, a impureza do desejo sensual que
ainda não surgiu, não surge nela e a impureza do desejo sensual que já surgiu é
abandonada; a impureza de existir/ser que ainda não surgiu, não surge nela e a
impureza de existir/ser que já surgiu é abandonada; a impureza da ignorância
que ainda não surgiu, não surge nela e a impureza da ignorância que já surgiu é
abandonada. Essas são as coisas que merecem atenção mas com as quais ela não se
ocupa. Ocupando-se com coisas que não merecem atenção e não se ocupando com
coisas que merecem atenção, ambas, as impurezas que ainda não surgiram, surgem
e as impurezas que já surgiram, aumentam.<o:p></o:p></span></p>

<p class=Normal><a name=T7></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>7. É desta forma que ela se ocupa sem sabedoria: ‘Eu existi no passado?
Não existi no passado? O que fui no passado? Como eu era no passado? Tendo sido
que, no que me tornei no passado? Existirei no futuro? Não existirei no futuro?
O que serei no futuro? Como serei no futuro? Tendo sido que, no que me tornarei
no futuro?’ Ou então ela está no seu íntimo perplexa acerca do presente: ‘Eu
sou? Eu não sou? O que sou? Como sou? De onde veio este ser? Para onde irá?’ [<a
href="#N6">6</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R6></a><a name=T8></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>8. “Quando ela se ocupa dessa forma, sem
sabedoria, uma entre seis idéias surgem nela. [<a href="#N7">7</a>] <a name=R7></a>A
idéia de que ‘um eu existe em mim’ surge como verdadeira e consagrada; ou a
idéia de que ‘um eu não existe em mim’ surge como verdadeira e consagrada; ou a
idéia de que ‘eu percebo o eu através do eu’ surge como verdadeira e
consagrada; ou a idéia de que ‘eu percebo o não-eu através do eu’ surge como
verdadeira e consagrada; ou a idéia de que ‘eu percebo o eu através do não-eu’
surge como verdadeira e consagrada; ou então ela tem uma idéia como esta: ‘É
esse meu eu que fala e sente e experimenta aqui e ali o resultado de boas e más
ações; mas esse meu eu é permanente, interminável, eterno, não sujeito à
mudança e que irá durar tanto tempo quanto a eternidade.’ [<a href="#N8">8</a>]
<a name=R8></a>Essas idéias especulativas, bhikkhus, se denominam um emaranhado
de idéias, uma confusão de idéias, idéias contorcidas, idéias vacilantes,
idéias que agrilhoam. Aprisionado pelas<span style="mso-spacerun: yes"> 
</span>idéias que agrilhoam, a pessoa comum sem instrução não se vê livre do
nascimento, envelhecimento e morte, da tristeza, lamentação, dor, angústia e desespero;
ela não se vê livre do sofrimento, eu digo.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9.
“Bhikkhus, um nobre discípulo bem instruído, que respeita os nobres, que é
proficiente e treinado no Dhamma deles, que respeita os homens verdadeiros,
que é proficiente e treinado no Dhamma deles, entende o tipo de coisas que merecem atenção e que tipo de coisas não merecem
atenção. Assim sendo, ele não se ocupa com aquelas coisas que não merecem
atenção e se ocupa com as coisas que merecem atenção.<o:p></o:p></span></p>




<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10.
“Quais são as coisas que não merecem atenção com as quais ele não se ocupa? Elas são coisas tais que quando ele se ocupa com elas, a impureza do
desejo sensual que ainda não surgiu, surge nele ... (igual ao verso 6) ... e a
impureza da ignorância que já surgiu aumenta nele. Essas são as coisas que não
merecem atenção com as quais ele não se ocupa. E quais são as coisas que
merecem atenção com as quais ele se ocupa? Elas são coisas tais que quando ele
se ocupa com elas, a impureza do desejo sensual que ainda não surgiu, não surge
nele ... (igual ao verso 6) ... e a impureza da ignorância que já surgiu é
abandonada<span style="mso-spacerun: yes">  </span>Essas são as coisas dignas
de atenção com as quais ele se ocupa. Não se ocupando com coisas que não
merecem atenção e ocupando-se com coisas que merecem atenção, as impurezas que
ainda não surgiram, não surgem e as impurezas que já surgiram, são abandonadas.</span><span
lang=PT-BR style='mso-ansi-language:PT-BR'><o:p></o:p></span></p>

<p class=Normal><a name=T11></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. “Ele
aplica sua atenção com sabedoria: ‘Isto é sofrimento’; ele aplica a sua atenção
com sabedoria: ‘Esta é a origem do sofrimento’; ele aplica a sua atenção com
sabedoria: ‘Esta é a cessação do sofrimento’; ele aplica a sua atenção com
sabedoria: ‘Este é o caminho que conduz à cessação do sofrimento’. [<a
href="#N9">9</a>] <a name=R9></a>Quando ele aplica a sua atenção com sabedoria
desta forma, três grilhões são abandonados: a idéia da existência de um eu, a
dúvida e o apego a preceitos e rituais. Essas são chamadas as impurezas que
devem ser abandonadas através da visão. [<a href="#N10">10</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R10></a><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(Impurezas
que devem ser abandonadas pelo autocontrole)</b><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. ‘Quais impurezas,
bhikkhus, devem ser abandonadas pelo autocontrole? [<a href="#N11">11</a>] <a
name=R11></a>Neste caso um bhikkhu, refletindo de maneira sábia, permanece com
a faculdade do olho controlada. Enquanto que impurezas, aflição e febre podem
surgir naquele que permanece com a faculdade do olho sem controle, não existem
impurezas, aflição ou febre naquele que permanece com a faculdade do olho
controlada. [<a href="#N12">12</a>] <a name=R12></a>Refletindo de maneira sábia
ele permanece com a faculdade do ouvido controlada ... com a faculdade do nariz
controlada ... com a faculdade da língua controlada ... com a faculdade do
corpo controlada ... com a faculdade da mente controlada ... Enquanto que
impurezas, aflição e febre podem surgir naquele que permanece com a
faculdade da mente sem controle, não existem impurezas, aflição ou febre naquele que
permanece com a faculdade da mente controlada. Essas são chamadas as impurezas que
devem ser abandonadas pelo autocontrole.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(Impurezas que devem ser
abandonadas pelo uso)</b><o:p></o:p></span></p>

<p class=Normal><a name=T13></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. “Quais
impurezas, bhikkhus, devem ser abandonadas pelo uso? [<a href="#N13">13</a>] <a
name=R13></a>Neste caso um bhikkhu, refletindo de maneira sábia, usa o seu
manto somente para proteção do frio, para proteção do calor, para proteção das
moscas, mosquitos, vento, sol e criaturas rastejantes e somente com o propósito
de ocultar as partes íntimas.<o:p></o:p></span></p>

<p class=Normal><a name=T14></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>14.
“Refletindo de maneira sábia, ele não usa os alimentos esmolados nem para
diversão nem para embriaguez, tampouco com o objetivo de embelezamento e para
ser mais atraente, somente com o propósito de manter a resistência e
continuidade desse corpo, como forma de dar um fim ao desconforto e para
auxiliar a vida santa, considerando: </span>‘<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Dessa forma darei um fim às antigas sensações (de fome) sem despertar
novas sensações (de comida em excesso) e serei saudável e sem culpa e viverei
em comodidade</span>.’” <span lang=PT-BR style='mso-ansi-language:PT-BR'><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. “Refletindo de maneira
sábia, ele usa a sua moradia somente para proteção do frio, para proteção do
calor, para proteção do contato com moscas, mosquitos, vento, sol e criaturas
rastejantes e somente com o propósito de evitar os perigos do clima e para desfrutar
do isolamento.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16. Refletindo de maneira
sábia, ele usa medicamentos somente para proteção contra sensações aflitivas
que já surgiram e para se beneficiar da boa saúde.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17. “Enquanto que
impurezas, aflição e febre podem surgir naquele que não satisfaz as suas
necessidades desta forma, não existem impurezas, aflição e febre naquele que
as satisfaz desta forma. Essas são chamadas as impurezas que devem ser
abandonadas pelo uso.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(Impurezas que devem ser
abandonadas com a paciência)</b><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18. “Quais impurezas,
bhikkhus, devem ser abandonadas com a paciência? Neste caso um bhikkhu,
refletindo de maneira sábia, agüenta o frio e o calor, fome e sede, o contato
com moscas, mosquitos, vento, sol e criaturas rastejantes; ele agüenta palavras
ditas de forma grosseira, desagradável e sensações no corpo que são dolorosas,
penetrantes, torturantes, desagradáveis, perigosas e que ameaçam a vida.
Enquanto que impurezas, aflição e febre podem surgir naquele que não agüenta
essas coisas, não existem impurezas, aflição e febre naquele que as agüenta.
Essas são chamadas as impurezas que devem ser abandonadas com a paciência.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(Impurezas que devem ser
abandonadas evitando)</b><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>19. “Quais impurezas,
bhikkhus, devem ser abandonadas evitando? Neste caso, um bhikkhu refletindo de
maneira sábia, evita um elefante selvagem, um cavalo selvagem, um touro
selvagem, um cão selvagem, uma cobra, caminhos irregulares e <span
style="mso-spacerun: yes"> </span>espinhosos, um precipício, um penhasco, uma
fossa, um esgoto. Refletindo de maneira sábia, ele evita sentar-se em assentos
inadequados [<a href="#N14">14</a>], <a name=R14></a>vagar por lugares
inadequados, associar-se a más companhias pois se ele assim o fizesse os
companheiros sábios na vida santa poderiam suspeitar que a sua conduta fosse
má. Enquanto que impurezas, aflição e febre podem surgir naquele que não evita
essas coisas, não existem impurezas, aflição e febre naquele que as evita.
Essas são chamadas as impurezas que devem ser abandonadas evitando.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(Impurezas que devem ser
abandonadas pela remoção)</b><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>20. “Quais impurezas,
bhikkhus, devem ser abandonadas pela remoção? Neste caso um bhikkhu, refletindo
de maneira sábia, não tolera um pensamento de desejo sensual que tenha surgido;
ele o abandona, o remove, o elimina, o aniquila. Ele não tolera um pensamento
de má vontade que tenha surgido ... Ele não tolera um pensamento de crueldade
... Ele não tolera estados ruins e prejudiciais que tenham surgido; ele os
abandona, os remove, os elimina, os aniquila. [<a href="#N15">15</a>] <a
name=R15></a>Enquanto que impurezas, aflição e febre podem surgir naquele que
não remove esses pensamentos, não existem impurezas, aflição e febre naquele
que os remove. Essas são chamadas as impurezas que devem ser abandonadas pela
remoção.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(Impurezas que devem ser
abandonadas com a meditação)</b><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>21. Quais impurezas,
bhikkhus, devem ser abandonadas com a meditação? Neste caso um bhikkhu,
com sabedoria intenciona o fator da iluminação da atenção plena,
que tem como base o afastamento, desapego e cessação que amadurece no abandono ...
 o fator da iluminação da investigação dos fenômenos ... o fator da
iluminação da energia ... o fator da iluminação do êxtase ... o fator da
iluminação da tranquilidade ... o fator da iluminação da concentração ... o
fator da iluminação da equanimidade que tem como base o afastamento, desapego e
cessação que amadurece no abandono. [<a href="#N16">16</a>] <a name=R16></a>Enquanto
que impurezas, aflição e febre podem surgir naquele que não desenvolve esses fatores
da iluminação, não existem impurezas, aflição e febre naquele que os
desenvolve. Essas são chamadas as impurezas que devem ser abandonadas com a meditação. [<a href="#N17">17</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R17></a><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(Conclusão)</b><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>22. “Bhikkhus, quando em um
bhikkhu as impurezas que deveriam ser abandonadas pela visão foram abandonadas
pela visão, quando as impurezas que deveriam ser abandonadas pelo autocontrole
foram abandonadas pelo autocontrole, quando as impurezas que deveriam ser
abandonadas pelo uso foram abandonadas pelo uso, quando as impurezas que
deveriam ser abandonadas com a paciência foram abandonadas com a paciência, quando as
impurezas que deveriam ser abandonadas evitando foram abandonadas
evitando, quando as impurezas que deveriam ser abandonadas pela remoção
foram abandonadas pela remoção, quando as impurezas que deveriam ser abandonadas com a meditação foram abandonadas com a meditação. – então
ele é denominado um bhikkhu que permanece controlado com o controle de todas as
impurezas. Ele cortou o desejo, rompeu os grilhões e penetrando completamente a
presunção deu um fim ao sofrimento.” [<a href="#N18">18</a>]<o:p></o:p></span></p>

