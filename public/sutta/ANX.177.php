<p class=Normal> Então o brâmane Janussoni foi até o Abençoado e ambos se cumprimentaram. Quando a conversa amigável e cortês havia terminado, ele sentou a um lado e disse: </p>

<p class=Normal><a name=R1></a>"Mestre Gotama, nós brâmanes damos oferendas e realizamos cerimônias para os mortos com o pensamento: [<a href="#N1">1</a>] 'Que as nossas oferendas beneficiem nossos parentes e familiares que partiram. Que nossos parentes e familiares que partiram recebam a nossa oferenda.' A nossa oferenda, Mestre Gotama, pode na verdade beneficiar os nossos parentes e familiares que partiram? Os nossos parentes e familiares que partiram podem na verdade receber a nossa oferenda?" </p>

<p class=Normal>"Na ocasião certa, brâmane, poderá beneficiar, não na ocasião errada." </p>

<p class=Normal>"Mas, Mestre Gotama, qual é a ocasião certa e qual é a ocasião errada?" </p>

<p class=Normal>"Aqui, brâmane, alguém mata seres vivos, toma aquilo que não é dado, tem conduta sexual imprópria, emprega a linguagem mentirosa, a linguagem maliciosa, a linguagem grosseira, a linguagem frívola, é cobiçoso,  tem má vontade, e tem entendimento incorreto. Com a dissolução do corpo, após a morte, ele renasce no inferno. Ele se sustenta e subsiste da comida dos seres no inferno. Essa é uma ocasião errada, quando a oferenda não irá beneficiar aquele que ali se encontra. </p>

<p class=Normal>"Alguém mata seres vivos ... tem entendimento incorreto. Com a dissolução do corpo, após a morte, ele renasce no mundo animal. Ele se sustenta e subsiste da comida dos animais. Essa também é uma ocasião errada, quando a oferenda não irá beneficiar aquele que ali se encontra. </p>

<p class=Normal>"Alguém se abstém de matar seres vivos, de tomar aquilo que não é dado, da conduta sexual imprópria, da linguagem mentirosa, da linguagem maliciosa, da linguagem grosseira, da linguagem frívola, não é cobiçoso,  tem boa vontade, e tem entendimento correto. Com a dissolução do corpo, após a morte, ele renasce entre os seres humanos. Ele se sustenta e subsiste da comida dos seres humanos. Essa é uma ocasião errada, quando a oferenda não irá beneficiar aquele que ali se encontra. </p>

<p class=Normal>"Alguém se abstém de matar seres vivos ... tem entendimento correto. Com a dissolução do corpo, após a morte, ele renasce entre os devas. Ele se sustenta e subsiste da comida dos devas. Essa é uma ocasião errada, quando a oferenda não irá beneficiar aquele que ali se encontra. </p>

<p class=Normal>"Alguém mata seres vivos ... tem entendimento incorreto. Com a dissolução do corpo, após a morte, ele renasce no mundo dos fantasmas famintos. Ele se sustenta e subsiste da comida dos fantasmas famintos, ou então ele se sustenta com aquilo que os seus amigos, companheiros, parentes ou familiares neste mundo lhe oferecem. Essa é uma ocasião certa, quando a oferenda irá beneficiar aquele que ali se encontra." </p>

<p class=Normal>"Mas, Mestre Gotama, quem se beneficia da oferenda se o parente ou familiar do falecido não renasceram nesse mundo?" </p>

<p class=Normal>"Outros parentes ou familiares falecidos que renasceram nesse mundo se beneficiam da oferenda." </p>

<p class=Normal>"Mas, Mestre Gotama, quem se beneficia da oferenda se nem os parentes ou familiares do falecido tampouco outros tenham renascido nesse mundo?" </p>

<p class=Normal>"Brâmane, durante este longo período de tempo [no samsara] é impossível e inconcebível que aquele mundo esteja desprovido de parentes ou familiares falecidos. Além disso, o doador também não fica desprovido de frutos." </p>

<p class=Normal>"O Mestre Gotama postula [o valor da generosidade] mesmo na ocasião errada?" </p>

<p class=Normal>"Brâmane, eu postulo [o valor da generosidade] mesmo na ocasião errada. </p>

<p class=Normal>"Aqui, Brâmane, alguém mata seres vivos, toma aquilo que não é dado, tem conduta sexual imprópria, emprega a linguagem mentirosa, a linguagem maliciosa, a linguagem grosseira, a linguagem frívola, é cobiçoso,  tem má vontade, e tem entendimento incorreto. Ele dá para um contemplativo ou brâmane comida, bebida; roupas, um veículo; ornamentos, perfume e ungüento; roupas de cama, moradia, e uma lamparina. Com a dissolução do corpo, após a morte, ele renasce entre os elefantes. Ali ele obtém comida e bebida, e vários ornamentos. </p>

<p class=Normal>"Visto que aqui ele matou seres vivos ... teve entendimento incorreto, com a dissolução do corpo, após a morte, ele renasce entre os elefantes. Mas visto que ele deu para um contemplativo ou brâmane comida, bebida ... ele ali obtém comida e bebida, e vários ornamentos. </p>

<p class=Normal>"Alguém mata seres vivos ... tem entendimento incorreto. Ele dá para um contemplativo ou brâmane comida, bebida ... lamparina. Com a dissolução do corpo, após a morte, ele renasce entre os cavalos ... bois ... cachorros. Ali ele obtém comida e bebida, e vários ornamentos. </p>

<p class=Normal>"Visto que aqui ele matou seres vivos ... teve entendimento incorreto, com a dissolução do corpo, após a morte, ele renasce entre os cavalos ... bois ... cachorros. Mas visto que ele deu para um contemplativo ou brâmane comida, bebida ... ele ali obtém comida e bebida, e vários ornamentos. </p>

<p class=Normal>"Alguém se abstém de matar seres vivos, de tomar aquilo que não é dado, da conduta sexual imprópria, da linguagem mentirosa, da linguagem maliciosa, da linguagem grosseira, da linguagem frívola, não é cobiçoso,  tem boa vontade, e tem entendimento correto. Ele dá para um contemplativo ou brâmane comida, bebida; roupas, um veículo; ornamentos, perfume e ungüento; roupas de cama, moradia, e uma lamparina. Com a dissolução do corpo, após a morte, ele renasce entre os seres humanos. Ali ele obtém os cinco elementos do prazer sensual humano. </p>

<p class=Normal>"Visto que aqui ele se absteve de matar seres vivos ... teve entendimento correto, com a dissolução do corpo, após a morte, ele renasce entre os seres humanos. E visto que ele deu para um contemplativo ou brâmane comida, bebida ... ele ali obtém os cinco elementos do prazer sensual humano. </p>

<p class=Normal>"Alguém se abstém de matar seres vivos, ... tem entendimento correto. Ele dá para um contemplativo ou brâmane comida, bebida; ... lamparina. Com a dissolução do corpo, após a morte, ele renasce entre os devas. Ali ele obtém os cinco elementos do prazer celestial. </p>

<p class=Normal>"Visto que aqui ele se absteve de matar seres vivos ... teve entendimento correto, com a dissolução do corpo, após a morte, ele renasce entre os devas. E visto que ele deu para um contemplativo ou brâmane comida, bebida ... ele ali obtém os cinco elementos do prazer celestial. [Foi por isso que eu disse:] 'Além disso, o doador também não fica desprovido de frutos'." </p>

<p class=Normal>"É admirável e maravilhoso, Mestre Gotama, que há razão em dar oferendas e realizar cerimônias em memória dos mortos, visto que o doador também não fica desprovido de frutos." </p>

<p class=Normal>"Assim é, brâmane! Assim é, brâmane! O doador também não fica desprovido de frutos." </p>

<p class=Normal>Magnífico, Mestre Gotama! Magnífico, Mestre Gotama! Mestre Gotama esclareceu o Dhamma de várias formas, como se tivesse colocado em pé o que estava de cabeça para baixo, revelasse o que estava escondido, mostrasse o caminho para alguém que estivesse perdido ou segurasse uma lâmpada no escuro para aqueles que possuem visão pudessem ver as formas. Eu busco refúgio no Mestre Gotama, no Dhamma e na Sangha dos bhikkhus. Que o Mestre Gotama me aceite como discípulo leigo que nele buscou refúgio para o resto da minha vida." </p>


