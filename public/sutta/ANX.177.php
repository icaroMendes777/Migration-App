<p class=Normal><b>Notas:</b></p>

<p class=Normal><a name=N1></a> [1] <i>Sraddha</i>: São cerimônias realizadas pelos brâmanes com grande rigor, em intervalos de tempo pré-fixados, que compreendem festejos bem como luto, em honra e benefício dos parentes mortos observadas pelos parentes vivos. <i>Sraddha</i> não é uma cerimônia fúnebre mas um complemento a essa cerimônia; é uma ato de homenagem reverencial para a pessoa falecida realizada pelos parentes; os alimentos oferecidos durante a <i>sraddha</i> supostamente irão proporcionar à falecida o nutrimento fortificante depois que a realização das cerimônias funerárias lhe proporcionaram um corpo etéreo; na verdade até que esses 'ritos funerários' tenham sido realizados e até que a primeira <i>sraddha</i> tenha sido celebrada a pessoa morta é um <i>preta</i> ou inquieta, um fantasma errante. Após a celebração da primeira <i>sraddha</i> a falecida alcança um lugar entre os <i>pitr</i> ou divinos ancestrais. A <i>sraddha</i> é mais desejável e eficaz quando realizada por um filho. [<a href="#R1">Retorna</a>] </p>

