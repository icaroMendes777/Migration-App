<p class=Normal>Assim ouvi. Em certa ocasião, o Abençoado estava viajando em Kosala com venerável Nagasamala como seu assistente júnior. Enquanto seguia pela estrada o venerável Nagasamala viu uma bifurcação. Ao vê-la o venerável Nagasamala disse para o Abençoado: "Este, Abençoado senhor, é o caminho. Sigamos nesta direção.”</p>



<p class=Normal>Uma segunda vez, o venerável Nagasamala disse para o Abençoado: "Este, Abençoado senhor, é o caminho. Sigamos nesta direção.”</p>



<p class=Normal>Uma terceira vez, o venerável Nagasamala disse para o Abençoado: "Este, Abençoado senhor, é o caminho. Sigamos nesta direção.”</p>



<p class=Normal>Então, o venerável Nagasamala, devolvendo ao Abençoado sua tigela e mantos, bem ali, partiu dizendo, "Aqui está, Abençoado senhor, a tigela e os mantos.”</p>



<p class=Normal>Agora, enquanto o venerável Nagasamala seguia por aquela rota ladrões - surgidos no meio da estrada - assaltaram-no com socos e pontapés, quebrando sua tigela e retalhando seu manto externo.</p>



<p class=Normal>Então, o venerável Nagasamala - com sua tigela quebrada e mantos externos retalhados - foi até o Abençoado e disse: "Agora mesmo, senhor, enquanto eu seguia por aquela rota ladrões - surgidos no meio da estrada - assaltaram-no com socos e pontapés, quebrando minha tigela e retalhando meu manto externo.”</p>



<p class=Normal>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p>



	<p class=Normal style='margin-left:.75in'>"Quando viajando acompanhado<br>

	por uma pessoa ignorante,<br>

	aquele que é sábio,<br>

	ao compreender que esta pessoa não lhe faz bem,<br>

	a abandona,<br>

	da mesma forma que uma jovem garça,<br>

	deixa o pântano.”</p>
