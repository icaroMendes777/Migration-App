<p class=Normal> “Alguém que tenha virtude, não é necessário o pensamento intencional: ‘Que a  libertação do arrependimento surja em mim!’ É uma lei da natureza, bhikkhus, que  naquele que tem virtude surge a libertação do arrependimento. </p>

<p class=Normal> “Alguém que está livre do arrependimento, não é necessário o pensamento intencional: ‘Que a satisfação surja em mim!’ É uma lei da natureza, bhikkhus, que  naquele que está livre do arrependimento surge a satisfação. </p>

<p class=Normal> “Alguém que está satisfeito, não é necessário o pensamento intencional: ‘Que o êxtase surja em mim!’ É uma lei da natureza, bhikkhus, que  naquele que está satisfeito surge o êxtase. </p>

<p class=Normal> “Alguém cujo coração está extasiado, não é necessário o pensamento intencional: ‘Que meu corpo se acalme!’ É uma lei da natureza, bhikkhus, que  naquele cujo coração está extasiado o corpo é acalmado. </p>

<p class=Normal> “Alguém cujo corpo está calmo, não é necessário o pensamento intencional: ‘Que eu sinta felicidade!’ É uma lei da natureza, bhikkhus, que  naquele cujo corpo está calmo, sente felicidade. [<a href="#N1">1</a>]</p>

<p class=Normal><a name="R1"> </a> “Alguém que sente felicidade, não é necessário o pensamento intencional: ‘Que a minha mente fique concentrada!’ É uma lei da natureza, bhikkhus, que  naquele que sente felicidade, a mente se concentra. </p>

<p class=Normal> “Alguém com a mente concentrada, não é necessário o pensamento intencional: ‘Que eu conheça e veja as coisas como elas na verdade são!’ É uma lei da natureza, bhikkhus, que  uma mente concentrada conhece e vê as coisas como elas na verdade são. </p>

<p class=Normal> “Alguém que conhece e vê as coisas como elas na verdade são, não é necessário o pensamento intencional: ‘Que eu experimente o desencantamento!’ É uma lei da natureza, bhikkhus, que  aquele que vê as coisas como elas na verdade são, experimenta o desencantamento. </p>

<p class=Normal> “Alguém que experimenta o desencantamento, não é necessário o pensamento intencional: ‘Que eu experimente o desapego!’ É uma lei da natureza, bhikkhus, que aquele que experimenta o desencantamento, experimenta o desapego. </p>

<p class=Normal> “Alguém que experimenta o desapego, não é necessário o pensamento intencional: ‘Que eu realize o conhecimento e visão da libertação!’ É uma lei da natureza, bhikkhus, que aquele que experimenta o desapego, realiza o conhecimento e visão da libertação. </p>

<p class=Normal> “Portanto, bhikkhus, o desapego possui o conhecimento e visão da libertação como seu benefício e recompensa, o desencantamento possui o desapego como seu benefício e recompensa, conhecer e ver as coisas como elas na verdade são possui o desencantamento como seu benefício e recompensa, a concentração possui conhecer e ver as coisas como elas na verdade são como seu benefício e recompensa, sentir felicidade possui a concentração como seu benefício e recompensa, o corpo calmo possui sentir felicidade como seu benefício e recompensa, o coração extasiado possui o corpo calmo como seu benefício e recompensa, a satisfação possui o êxtase como seu benefício e recompensa, estar livre do arrependimento possui a satisfação como seu benefício e recompensa,  a conduta com virtude tem a libertação do arrependimento como seu benefício e recompensa. </p>

<p class=Normal> “Assim, bhikkhus, as qualidades que precedem fluem para as qualidades que sucedem; as qualidades que sucedem conduzem à perfeição as qualidades que precedem - para cruzar desta margem para a outra margem. </p>

