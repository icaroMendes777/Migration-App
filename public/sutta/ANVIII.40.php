<p class=Normal> (1) "Bhikkhus, matar seres vivos, repetidamente perseguido, desenvolvido, e cultivado, conduz ao inferno, ao mundo animal, ao mundo dos fantasmas famintos; para quem renasça como ser humano matar seres vivos conduz no mínimo a um tempo de vida curto. </p>

<p class=Normal> (2) "Tomar o que não foi dado, repetidamente perseguido, desenvolvido, e cultivado, conduz ao inferno, ao mundo animal, ao mundo dos fantasmas famintos; para quem renasça como ser humano tomar o que não foi dado conduz no mínimo à perda de riqueza. </p>

<p class=Normal> (3) "A conduta sexual imprópria, repetidamente perseguida, desenvolvida, e cultivada, conduz ao inferno, ao mundo animal, ao mundo dos fantasmas famintos; para quem renasça como ser humano a conduta sexual imprópria conduz no mínimo à inimizade e rivalidade. </p>

<p class=Normal> (4) "A linguagem falsa, repetidamente perseguida, desenvolvida, e cultivada, conduz ao inferno, ao mundo animal, ao mundo dos fantasmas famintos; para quem renasça como ser humano a linguagem falsa conduz no mínimo a acusações falsas. </p>

<p class=Normal> (5) "A linguagem maliciosa, repetidamente perseguida, desenvolvida, e cultivada, conduz ao inferno, ao mundo animal, ao mundo dos fantasmas famintos; para quem renasça como ser humano a linguagem maliciosa conduz no mínimo à separação dos amigos. </p>

<p class=Normal> (6) "A linguagem grosseira, repetidamente perseguida, desenvolvida, e cultivada, conduz ao inferno, ao mundo animal, ao mundo dos fantasmas famintos; para quem renasça como ser humano a linguagem grosseira conduz no mínimo a sons desagradáveis. </p>

<p class=Normal> (7) "A linguagem frívola, repetidamente perseguida, desenvolvida, e cultivada, conduz ao inferno, ao mundo animal, ao mundo dos fantasmas famintos; para quem renasça como ser humano a linguagem frívola conduz no mínimo ao descrédito daquilo que é dito. </p>

<p class=Normal> (8) "Beber vinho, álcool e outros embriagantes que causam a negligência,, repetidamente perseguido, desenvolvido, e cultivado, conduz ao inferno, ao mundo animal, ao mundo dos fantasmas famintos; para quem renasça como ser humano beber vinho, álcool e outros embriagantes que causam a negligência conduz no mínimo à loucura."
</p>

