<p class=Normal> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: "Bhikkhus, um bhikkhu que possui bela virtude, bela conduta e bela sabedoria é chamado neste Dhamma e Disciplina alguém que é plenamente realizado, que alcançou a perfeição, supremo entre os humanos.</p>

<p class=Normal> "Como um bhikkhu possui bela virtude? Aqui, um bhikkhu permanece possuído pela virtude, possuído pelo Patimokkha, contido pelas regras do Patimokkha, perfeito na sua conduta e na sua esfera de atividades, temendo a menor falha, ele treina adotando os preceitos de virtude. Desse modo um bhikkhu é aquele que possui bela virtude. Assim a sua virtude é bela.</p>

<p class=Normal> "Como um bhikkhu possui bela conduta? Aqui, um bhikkhu permanece cultivando os sete fatores da iluminação. Desse modo um bhikkhu é aquele que possui bela conduta. Assim a sua conduta é bela.</p>

<p class=Normal> "Como um bhikkhu possui bela sabedoria? Aqui, através do conhecimento direto, um bhikkhu aqui e agora entra e permanece na libertação da mente e libertação através da sabedoria que são imaculadas com a destruição de todas as impurezas. Desse modo um bhikkhu é aquele que possui bela sabedoria. Assim a sua sabedoria é bela.</p>

<p class=Normal> "Assim ele possui bela virtude, bela conduta e bela sabedoria. Neste Dhamma e Disciplina ele é chamado alguém que é plenamente realizado, que alcançou a perfeição, supremo entre os humanos."</p>

<p class=Normal style='margin-left:.75in'> Um bhikkhu com senso de vergonha, <br>
que nunca faz algo errado de nenhuma forma, <br>
Nem com o corpo, linguagem, ou mente, <br>
é chamado "aquele com bela virtude."</p>

<p class=Normal style='margin-left:.75in'>Um bhikkhu sem presunção, <br>
que cultivou bem os estados<br>
que conduzem à iluminação<br>
é chamado "aquele com bela conduta."</p>

<p class=Normal style='margin-left:.75in'>Um bhikkhu livre das impurezas, <br>
que aqui e agora compreende por si mesmo<br>
o fim do sofrimento <br>
é chamado "aquele com bela sabedoria."</p>

<p class=Normal style='margin-left:.75in'>Aquele que brilha nessas três coisas, <br>
imperturbável, tendo destruído a dúvida, <br>
desapegado de tudo no mundo, <br>
é chamado "aquele que abandonou tudo."</p>



