derramando lágrimas por estarem unidos ao que é desagradável, separados do que
é agradável - não a água contida nos quatro grandes oceanos. <o:p></o:p></span></p>


<p class=Normal>&quot;Há muito tempo ( repetidamente ) vocês experimentaram a morte de
