grandes oceanos.&quot; <o:p></o:p></span></p>


<p class=Normal>&quot;Há muito tempo ( repetidamente ) vocês experimentaram a morte de
uma mãe. Isto é maior: as lágrimas que vocês derramaram pela morte de uma mãe
