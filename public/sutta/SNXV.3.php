enfermidades. Isto é maior: as lágrimas que vocês derramaram pelas perdas
por enfermidades enquanto transmigravam e perambulavam por este longo, longo
tempo - chorando e derramando lágrimas por estarem unidos ao que é
desagradável, separados do que é agradável - não a água contida nos quatro
grandes oceanos. <o:p></o:p></span></p>


<p class=Normal> &quot;Por que ocorre isso? Esse samsara não possui um início que possa
ser descoberto. Um ponto inicial não é discernido para os seres que seguem
vagando e perambulando, obstruídos pela ignorância e aprisionados pelo desejo.
Há muito tempo vocês experimentaram o sofrimento, experimentaram a dor,
experimentaram a perda, inchando os cemitérios - o suficiente para se
desencantarem com todas as coisas condicionadas, o suficiente para se tornarem
desapegados, o suficiente para se libertarem.&quot; <o:p></o:p></span></p>


