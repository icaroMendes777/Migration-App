<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião o Abençoado estava em Rajagaha, no Bambual, no Santuário
dos Esquilos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. Agora,
naquela ocasião o noviço Aciravata estava vivendo numa cabana na floresta.
Então o Príncipe Jayasena, enquanto perambulava e caminhava fazendo exercício,
foi até o noviço Aciravata e ambos se cumprimentaram. [<a href="#N1">1</a>] <a
name=R1></a>Quando a conversa amigável e cortês havia terminado, ele sentou a
um lado e disse para o noviço Aciravata: “Mestre Aggivessana, eu ouvi que um
bhikkhu que aqui permanece diligente, ardente e decidido pode alcançar a
unificação da mente.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Assim é,
príncipe, assim é. Um bhikkhu que aqui permanece diligente, ardente e decidido
pode alcançar a unificação da mente.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. “Seria
bom se o Mestre Aggivessana pudesse me ensinar o Dhamma tal como ele o ouviu e
compreendeu.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu não
posso ensinar-lhe o Dhamma, príncipe, tal como o ouvi e compreendi. Pois se eu lhe fosse ensinar o Dhamma tal como o ouvi e compreendi, você não iria entender o significado das minhas palavras e
isso seria fatigante e problemático para mim.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. “Que o
Mestre Aggivessana me ensine o Dhamma tal como ele o ouviu e compreendeu. Talvez eu possa entender o significado das suas palavras.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu
ensinarei o Dhamma, príncipe, tal como eu o ouvi e compreendi. Se você puder entender o significado das minhas palavras, isso
será bom. Mas se você não puder entender o significado, então deixe estar e não
me faça mais perguntas a respeito.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Que o
Mestre Aggivessana ensine o Dhamma para mim tal como ele o ouviu e compreendeu. Se eu puder entender o significado das suas palavras, isso
será bom. Se eu não puder entender o significado, então deixarei estar e não
farei mais perguntas a respeito.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. Então o
noviço Aciravata ensinou o Dhamma ao Príncipe Jayasena tal como ele o ouviu e
compreendeu. Depois de ele ter falado, o Príncipe Jayasena
observou: “É impossível, Mestre Aggivessana, não pode ser que um bhikkhu que
permanece diligente, ardente e decidido possa alcançar a unificação da mente.”
Então, tendo declarado ao noviço Aciravata que aquilo era impossível e que não
poderia acontecer, o Príncipe Jayasena levantou do seu assento e partiu.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. Pouco depois
do Príncipe Jayasena ter partido, o noviço Aciravata
foi até o Abençoado. Depois de cumprimentá-lo, ele<span
style='mso-spacerun:yes'>  </span>sentou a um lado e relatou toda a conversa
com o Príncipe Jayasena. Quando ele terminou, o Abençoado disse:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7.
“Aggivessana, como é possível que o Príncipe Jayasena, vivendo no meio dos
prazeres sensuais, desfrutando dos prazeres sensuais, sendo devorado por
pensamentos de prazeres sensuais, sendo consumido pela febre dos prazeres
sensuais, empenhado na busca por prazeres sensuais, possa saber, ver ou
compreender aquilo que deve ser entendido através da renúncia, visto através da
renúncia, alcançado através da renúncia, realizado através da renúncia? Isso é
impossível.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. “Suponha
[<a href="#N2">2</a>] <a name=R2></a>Aggivessana, que houvesse dois elefantes,
ou cavalos, ou bois, que estivessem bem domados e bem disciplinados, e dois
elefantes, ou cavalos, ou bois, que estivessem indomados e
indisciplinados.<span style='mso-spacerun:yes'>  </span>O que você pensa
Aggivessana? Os dois elefantes, ou cavalos, ou bois, que estivessem bem domados
e bem disciplinados, estando amansados eles adquiririam o comportamento dos
domados, eles obteriam o grau dos domados?” – “Sim, venerável senhor.” – “E os
dois elefantes, ou cavalos, ou bois, que estivessem indomados e
indisciplinados, estando indomados eles adquiririam o comportamento dos
domados, eles obteriam o grau dos domados, igual aos dois elefantes, ou
cavalos, ou bois que estavam bem domados e bem disciplinados?” – “Não,
venerável senhor.” – “Assim também, Aggivessana, é impossível que o Príncipe Jayasena,
vivendo no meio dos prazeres sensuais, ... possa saber, ver ou compreender
aquilo que deve ser entendido através da renúncia, visto através da renúncia,
alcançado através da renúncia, realizado através da renúncia.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9.
“Suponha, Aggivessana, que houvesse uma montanha alta não muito distante de um
vilarejo ou cidade e dois amigos saíssem do vilarejo ou cidade e fossem até a
montanha. Tendo chegado, um dos amigos permaneceria no pé da montanha enquanto
que o outro subiria até o topo. Então o amigo que permaneceu embaixo no pé da
montanha diria ao amigo que estava no topo: ‘Bem, amigo, o que você está vendo,
estando em pé no topo da montanha?’ E o outro responderia: ‘Em pé no topo da
montanha, amigo, eu vejo belos parques, belos bosques, belas campinas e belos
lagos.’ Então o primeiro amigo diria: ‘É impossível, amigo, não pode ser que
estando em pé no topo da montanha você possa ver belos parques, belos bosques,
belas campinas e belos lagos.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então o
outro amigo desceria até o pé da montanha, tomaria o seu amigo pelo braço e o
ajudaria a subir até o topo da montanha. Depois de dar a ele alguns momentos
para que recuperasse a respiração, ele perguntaria: ‘Bem, amigo, estando aqui
em pé no topo da montanha, o que você vê?’ E o seu amigo responderia: ‘Em pé no
topo da montanha, amigo, eu vejo belos parques, belos bosques, belas campinas e
belos lagos.’ Então o outro diria: ‘Amigo, há pouco ouvi você dizer: “É
impossível, amigo, não pode ser que estando em pé no topo da montanha você
possa ver belos parques, belos bosques, belas campinas e belos lagos.” Mas
agora mesmo ouvi você dizer: “Em pé no topo da montanha, amigo, eu vejo belos
parques, belos bosques, belas campinas e belos lagos.”’ Então o primeiro amigo
responderia: ‘Como eu estava obstruído por esta montanha elevada, amigo, eu não
via aquilo que havia para ser visto.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. “Assim
também, Aggivessana, o Príncipe Jayasena está obstruído, impedido, bloqueado e
envolvido por uma massa ainda maior que essa – a massa da ignorância. Portanto,
é impossível que o Príncipe Jayasena, vivendo no meio dos prazeres sensuais,
... possa saber, ver ou compreender aquilo que deve ser entendido através da
renúncia, visto através da renúncia, alcançado através da renúncia, realizado
através da renúncia.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. “Aggivessana,
se esses dois símiles lhe tivessem ocorrido com (relação a) o Príncipe
Jayasena, ele teria espontaneamente adquirido confiança em você, e tendo
confiança ele teria lhe mostrado confiança.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, como poderiam esses dois símiles ter ocorrido a mim com (relação a) o
Príncipe Jayasena da forma como eles ocorrem para o Abençoado, visto que eles
são espontâneos e nunca foram ouvidos antes?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12.
“Suponha, Aggivessana, que um nobre rei ungido se dirija ao seu mateiro da
seguinte forma: ‘Bom mateiro, monte no elefante real, entre na floresta de
elefantes e quando você vir um elefante da floresta, amarre-o pelo pescoço ao
elefante real.’ Tendo respondido ‘Sim, senhor,’ o mateiro monta no elefante
real, entra na floresta de elefantes e ao ver um elefante da floresta ele o
amarra pelo pescoço ao elefante real. O elefante real o conduz para o
descampado. É assim que um elefante da floresta vem para o descampado; pois o
elefante da floresta se apega à floresta dos elefantes.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então o
mateiro informa ao nobre rei ungido: ‘Senhor, o elefante da floresta veio para
o descampado.’ O rei se dirige ao seu domesticador de elefantes da seguinte
forma: ‘Venha, bom domesticador de elefantes, domestique o elefante da
floresta. Subjugue os seus hábitos da floresta, subjugue as suas memórias e
intenções da floresta, subjugue a sua aflição, cansaço e febre por ter deixado
a floresta. Faça com que ele se delicie com a cidade, inculque nele os hábitos
compatíveis com os seres humanos.’ Tendo respondido ‘Sim, senhor,’ o
domesticador de elefantes planta um grande poste na terra e amarra nele o
elefante pelo pescoço de modo a subjugar os seus hábitos da floresta … e para
inculcar nele os hábitos compatíveis com os seres humanos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então o
domesticador de elefantes se dirige ao elefante com palavras que são gentis,
que agradam aos ouvidos, carinhosas, que penetram o coração, que são corteses,
desejadas por muitos e que agradam a muitos. Quando o elefante da floresta é
dirigido com tais palavras, ele ouve, dá atenção e esforça a mente para
compreender. O domesticador de elefantes em seguida o recompensa com forragem e
água. Quando o elefante da floresta aceita dele a forragem e a água, o
domesticador de elefantes sabe: ‘Agora o elefante do rei irá viver!’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então o
domesticador de elefantes o treina mais assim: ‘Levante isto, abaixe isto!’
Quando o elefante real obedece as ordens do seu domesticador para levantar e
abaixar e executa as suas instruções, o domesticador de elefantes o treina mais
assim: ‘Vá para frente, vá para trás!’ Quando o elefante real obedece as ordens
do seu domesticador para ir para frente e para trás e executa as suas
instruções, o domesticador de elefantes o treina mais assim: ‘Fique em pé,
sente!’ Quando o elefante real obedece as ordens do seu domesticador para ficar
em pé e sentar e executa as suas instruções, o domesticador de elefantes o
treina mais na tarefa chamada imperturbabilidade. Ele amarra uma imensa tábua
na tromba dele; um homem com uma lança senta sobre o seu pescoço; homens com lanças
nas mãos o cercam por todos os lados; e o próprio domesticador de elefantes
fica em pé à frente dele segurando uma grande lança. Quando o elefante está
sendo treinado na tarefa da imperturbabilidade, ele não move as suas patas
dianteiras nem as traseiras; ele não move os seus quartos dianteiros nem os
traseiros; ele não move a cabeça, orelhas, presas, rabo ou tromba. O elefante
do rei é capaz de suportar os golpes de lanças, golpes de espadas, golpes de
flechas, golpes de outros seres, e o ruído retumbante de tambores, timbales,
trombetas e tantãs. Tendo eliminado todas as falhas e defeitos, purificado as
imperfeições, ele é digno do rei, de servir ao rei, considerado como um dos
fatores de um rei. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13-14.
“Assim também, Aggivessana, um Tathagata surge no mundo, um arahant, perfeitamente
iluminado ... (igual ao <a href="MN51.php#T12">MN 51, versos 12-13</a>) ...
raspa o seu cabelo e barba, veste o manto de cor ocre e segue a vida santa. É
dessa forma que um nobre discípulo vem para o descampado; pois devas e humanos
se apegam aos cinco elementos do prazer sensual.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. “Então
o Tathagata o disciplina mais da seguinte forma: <b>‘</b>Venha Bhikkhu, seja
virtuoso, contido pelas regras do Patimokkha, seja perfeito na conduta e na sua
esfera de atividades, temendo a menor falha, treine adotando os preceitos de
virtude.’ <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16.
“Quando, Aggivessana, o nobre discípulo é virtuoso ... teme a menor falha e
treina adotando os preceitos de virtude então o Tathagata o disciplina mais da
seguinte forma: ‘Venha Bhikkhu, ao ver uma forma com o olho, não se agarre aos
seus sinais ou detalhes. Visto que, se permanecer com a faculdade do olho
descuidada, você será tomado pelos estados ruins e prejudiciais de cobiça e
tristeza. Pratique a contenção, proteja a faculdade do olho, empenhe-se na
contenção da faculdade do olho. Ao ouvir um som com o ouvido ... Ao cheirar um
aroma com o nariz … Ao saborear um sabor com a língua … Ao tocar algo tangível
com o corpo … Ao conscientizar um objeto mental<span style='mso-spacerun:yes'> 
</span>com a<span style='mso-spacerun:yes'>  </span>mente, não se agarre aos
seus sinais ou detalhes. Visto que, se permanecer com a faculdade da mente
descuidada, você será tomado pelos estados ruins e prejudiciais de cobiça e
tristeza. Pratique a contenção, proteja a faculdade da mente, empenhe-se na
contenção da faculdade da mente.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17.
“Quando, Aggivessana, o nobre discípulo guarda as portas dos meios dos sentidos
então o Tathagata o disciplina mais da seguinte forma: ‘Venha Bhikkhu, seja moderado
na alimentação. Refletindo de maneira sábia, o alimento não deve ser tomado
como forma de diversão ou para embriaguez, tampouco com o objetivo de
embelezamento e para ser mais atraente, somente com o propósito de manter a
resistência e continuidade desse corpo, como forma de dar um fim ao desconforto
e para auxiliar a vida santa. Considerando: ‘Dessa forma darei um fim às
antigas sensações (de fome) sem despertar novas sensações (de comida em
excesso) e serei saudável e sem culpa e viverei em comodidade.’” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18.
“Quando, Aggivessana, o nobre discípulo é moderado na alimentação então o
Tathagata o disciplina mais da seguinte forma:<b> ‘</b>Venha Bhikkhu, seja
dedicado à vigilância. Durante o dia, enquanto estiver caminhando para lá e
para cá e sentado, purifique a sua mente dos estados obstrutivos. Na primeira
vigília da noite, e enquanto estiver caminhando para lá e para cá e sentado,
purifique a sua mente dos estados obstrutivos. Na segunda vigília da noite você
deve se deitar para dormir, no seu lado direito, na postura do leão com um pé
sobre o outro, atento e plenamente consciente, após anotar na sua mente o
horário para levantar. Após levantar-se,<span style='mso-spacerun:yes'> 
</span>na terceira vigília da noite, enquanto estiver caminhando para cá e para
lá e sentado, purifique a sua mente dos estados obstrutivos.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>19.
“Quando, Aggivessana, o nobre discípulo é vigilante então o Tathagata o
disciplina mais da seguinte forma: <b>‘</b>Venha Bhikkhu, possua atenção plena
e consciência plena. Aja com plena consciência ao ir para a frente e retornar;
aja com plena consciência ao olhar para frente e desviar o olhar; aja com plena
consciência ao dobrar e estender os membros; aja com plena consciência ao
carregar o manto externo, o manto superior, a tigela; aja com plena consciência
ao comer, beber, mastigar e saborear; aja com plena consciência ao urinar e
defecar; aja com plena consciência ao caminhar, ficar em pé, sentar, dormir,
acordar, falar e permanecer em silêncio.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>20.
“Quando, Aggivessana, o nobre discípulo possui atenção plena e consciência
plena então o Tathagata o disciplina mais da seguinte forma: <b>‘</b>Venha
Bhikkhu, busque um lugar isolado: na floresta, à sombra de uma árvore, uma
montanha, uma ravina, uma caverna em uma encosta, um cemitério, um matagal, um
espaço aberto, uma cabana vazia.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>21. “Ele
procura um local isolado: na floresta .... uma cabana. Depois de esmolar
alimentos, após a refeição, ele senta com as pernas cruzadas, mantém o corpo
ereto e estabelece a plena atenção à sua frente. Abandonando a cobiça pelo
mundo, ele permanece com a mente livre de cobiça; ele purifica sua mente da
cobiça. Abandonando a má vontade ele permanece com a mente livre de má vontade,
compadecido pelo bem-estar de todos seres vivos; ele purifica sua mente da má
vontade. Abandonando a preguiça e o torpor, ele permanece livre da preguiça e
do torpor, percebendo a luz, e plenamente consciente; ele purifica sua mente da
preguiça e do torpor. Abandonando a inquietação e a ansiedade, ele permanece 
calmo com a mente em paz; ele purifica sua mente da inquietação e da ansiedade. Abandonando 
a dúvida, ele assim permanece tendo superado a dúvida, sem perplexidade em relação a qualidades 
mentais hábeis; ele purifica a mente da dúvida.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>22. “Tendo
assim abandonado esses 5 obstáculos, imperfeições da mente que enfraquecem a
sabedoria, ele permanece contemplando o corpo como um corpo, ardente,
plenamente consciente e com atenção plena, tendo colocado de lado a cobiça e o
desprazer pelo mundo. Ele permanece contemplando as sensações como sensações
... a mente como mente ... objetos mentais como objetos mentais, ardente,
plenamente consciente e com atenção plena, tendo colocado de lado a cobiça e o
desprazer pelo mundo. [</span><a href="#N3"><span lang=PT-BR style='mso-ansi-language:
PT-BR'>3</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=R3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>23. “Da mesma forma, Aggivessana, que o domesticador de elefantes planta
um grande poste na terra e amarra nele o elefante pelo pescoço de modo a
subjugar os seus hábitos da floresta … e para inculcar nele os hábitos
compatíveis com os seres humanos, os quatro fundamentos da atenção plena são as
ataduras para a mente do nobre discípulo, para subjugar os seus hábitos
baseados na vida em família, subjugar as suas memórias e intenções da vida em
família, subjugar a sua aflição, cansaço e febre por ter deixado a vida em
família e para que ele possa realizar o verdadeiro caminho e realizar Nibbana.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>24. “Então
o Tathagata o disciplina mais: ‘Venha, bhikkhu, permaneça contemplando o corpo
como um corpo mas não pense pensamentos conectados com o corpo; permaneça
contemplando as sensações como sensações mas não pense pensamentos conectados
com as sensações; permaneça contemplando a mente como mente mas não pense
pensamentos conectados com a mente; permaneça contemplando objetos<span
style='mso-spacerun:yes'>  </span>mentais como objetos mentais mas não pense
pensamentos conectados com objetos mentais.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>25.
“Silenciando o pensamento aplicado e sustentado, um bhikkhu entra e permanece
no segundo jhana [<a href="#N4">4</a>] ... <a name=R4></a>no terceiro jhana ...
no quarto jhana.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>26-29.
&quot;Com a sua mente dessa forma concentrada, purificada ... (Igual ao </span><a
href="MN51.php#T24"><span lang=PT-BR style='mso-ansi-language:PT-BR'>MN 51,
versos 24-27</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>) ...
Ele compreende que ‘O nascimento foi destruído, a vida santa foi vivida, o que
deveria ser feito foi feito, não há mais vir a ser a nenhum estado.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>30. “Esse
bhikkhu é capaz de agüentar o frio e o calor, fome e sede, o contato com
moscas, mosquitos, vento, sol e criaturas rastejantes; ele é capaz de agüentar
palavras ditas de forma grosseira, desagradáveis e sensações no corpo que são
dolorosas, penetrantes, torturantes, desagradáveis, perigosas e que ameaçam a
vida. Tendo eliminado toda cobiça, raiva e delusão e purificado as
imperfeições, ele é merecedor de dádivas, merecedor de hospitalidade, merecedor
de oferendas, merecedor de saudações com reverência, um campo inigualável de
mérito para o mundo. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>31. “Se,
Aggivessana, o elefante real morre velho indomesticado e indisciplinado, então
ele é considerado um elefante velho que morreu uma morte indomesticada. Se o
elefante real morre na meia idade indomesticado e indisciplinado, então ele é
considerado um elefante na meia idade que morreu uma morte indomesticada. Se o
elefante real morre jovem indomesticado e indisciplinado, então ele é
considerado um elefante jovem que morreu uma morte indomesticada. Da mesma
forma, Aggivessana, se um bhikkhu sênior morre com as impurezas não destruídas,
então ele é considerado um bhikkhu sênior que morreu uma morte indomesticada.
Se um bhikkhu intermediário morre com as impurezas não destruídas, então ele é
considerado um bhikkhu intermediário que morreu uma morte indomesticada. Se um bhikkhu
júnior morre com as impurezas não destruídas, então ele é considerado um
bhikkhu júnior que morreu uma morte indomesticada.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>32. “Se,
Aggivessana, o elefante real morre velho bem domesticado e bem disciplinado,
então ele é considerado um elefante velho que morreu uma morte domesticada. Se
o elefante real morre na meia idade bem domesticado e bem disciplinado, então
ele é considerado um elefante na meia idade que morreu uma morte domesticada.
Se o elefante real morre jovem bem domesticado e bem disciplinado, então ele é
considerado um elefante jovem que morreu uma morte domesticada. Da mesma forma,
Aggivessana, se um bhikkhu sênior morre com as impurezas destruídas, então ele
é considerado um bhikkhu sênior que morreu uma morte domesticada. Se um bhikkhu
intermediário morre com as impurezas destruídas, então ele é considerado um bhikkhu
intermediário que morreu uma morte domesticada. Se um bhikkhu júnior morre com as
impurezas destruídas, então ele é considerado um bhikkhu júnior que morreu uma
morte domesticada.<o:p></o:p></span></p>



