<p class=Normal> Em certa ocasião o Abençoado estava em Savathi, no Bosque de Jeta, no Parque de Anathapindika. Lá ele se dirigiu aos monges desta forma: "Bhikkhus." – "Venerável Senhor," eles responderam. O Abençoado disse o seguinte: </p>

<p class=Normal> “Existem quatro tipos de nuvens carregadas de chuva. Quais quatro? Aquela que troveja porém não chove, aquela que chove porém não troveja, aquela que não troveja nem chove, e aquela que troveja e chove. Esses são os quatro tipos de nuvens carregadas de chuva. </p>

<p class=Normal> “Da mesma forma, existem quatro tipos de pessoas que podem ser encontradas no mundo que se assemelham a nuvens carregadas de chuva. Quais quatro? Aquela que troveja porém não chove, aquela que chove porém não troveja, aquela que não troveja nem chove, e aquela que troveja e chove. </p>

<p class=Normal> “E como é o tipo de pessoa que troveja porém não chove? É o caso em que alguém é um orador, não um realizador. Esse é o tipo de pessoa que troveja mas não chove. Esse tipo de pessoa, lhes digo, é como a nuvem que troveja mas não chove. </p>

<p class=Normal> “E como é o tipo de pessoa que chove porém não troveja? É o caso em que alguém é um realizador, não um orador. Esse é o tipo de pessoa que chove porém não troveja. Esse tipo de pessoa, lhes digo, é como a nuvem que chove porém não troveja. </p>

<p class=Normal> “E como é o tipo de pessoa que não troveja nem chove? É o caso em que alguém não é um orador nem é um realizador. Esse é o tipo de pessoa que não troveja nem chove. Esse tipo de pessoa, lhes digo, é como a nuvem que não troveja nem chove. </p>

<p class=Normal> “E como é o tipo de pessoa que troveja e chove? É o caso em que alguém é um orador e é um realizador. Esse é o tipo de pessoa que troveja e chove. Esse tipo de pessoa, lhes digo, é como a nuvem que troveja e chove. </p>

<p class=Normal> “Esses são os quatro tipos de pessoas que podem ser encontradas no mundo." </p>

