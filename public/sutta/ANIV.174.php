<p class=Normal>Então o ven. Maha Kotthita foi até o ven. Sariputta e ao chegar ambos se cumprimentaram. Após a troca de saudações corteses e amáveis, ele sentou a um lado e disse para o ven. Sariputta: “Com a cessação e desaparecimento sem deixar vestígios das seis bases do contato, (visão, audição, olfato, sabor, corpo e mente), é o caso de que existe algo mais?” </p>

<p class=Normal> [Sariputta:] “Não diga isso, meu amigo.” </p>

<p class=Normal> [Maha Kotthita:] “Com a cessação e desaparecimento sem deixar vestígios das seis bases do contato, é o caso de que não existe algo mais?” </p>

<p class=Normal> [Sariputta:] “Não diga isso, meu amigo.” </p>

<p class=Normal> [Maha Kotthita:] “... é o caso de ambos, existe e não existe algo mais?” </p>

<p class=Normal> [Sariputta:] “Não diga isso, meu amigo.” </p>

<p class=Normal> [Maha Kotthita:] “... é o caso de que nem existe, nem não existe algo mais?” </p>

<p class=Normal> [Sariputta:] “Não diga isso, meu amigo.” </p>

<p class=Normal> [Maha Kotthita:] “Sendo perguntado ... se existe algo mais, você diz 'Não diga isso, meu amigo'. Sendo perguntado … se não existe algo mais … se ambos, existe e não existe algo mais … se nem existe, nem não existe, você diz, 'Não diga isso, meu amigo'. Agora, como deve ser entendido o significado dessa afirmação?” </p>

<p class=Normal> [Sariputta:] “Dizendo, ‘Com a cessação e desaparecimento sem deixar vestígios das seis bases do contato, (visão, audição, olfato, sabor, corpo e mente), é o caso de que existe algo mais? … é o caso de que não existe algo mais … é o caso de ambos, existe e não existe algo mais … é o caso de que nem existe, nem não existe algo mais, está-se proliferando a não-proliferação. Até onde alcancem as seis bases do contato é até onde alcança a proliferação. Até onde alcance a proliferação é até onde alcançam as seis bases do contato. Com a cessação e desaparecimento sem deixar vestígio das seis bases do contato, ocorre a cessação, o alívio da proliferação.”[ <a href="# N1">1</a>] </p>

