<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] Com relação às proliferações e o seu papel na geração de conflitos,
veja o <a href="SnpIV.11.php">Snp IV.11</a> e o <a href="MN18.php">MN 18</a>. A
percepção, “Eu sou o pensador” está na raiz dessas proliferações no sentido que
interpreta e classifica o presente num conjunto de diferenciações – eu/não-eu;
ser/não ser; pensador/pensamento; identidade/não identidade – que depois se
multiplicam em conflitos físicos e mentais. A presunção inerente dessa forma
cria um grilhão na mente. Para poder se libertar é necessário aprender a
examinar essas diferenciações – que nós aceitamos como verdadeiras – para ver
que elas são simplesmente suposições que não são parte inerente da experiência,
e que estaríamos numa situação melhor se fôssemos capazes de abandoná-las. [<a
