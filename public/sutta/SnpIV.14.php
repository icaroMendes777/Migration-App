<span lang=PT-BR style='mso-ansi-language:
PT-BR'><span style='mso-spacerun:yes'>    </span>no interior ou exterior,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>ele deve evitar se tornar orgulhoso das suas convicções,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'><span style='mso-spacerun:yes'>    </span>pois isso não é chamado<br>

<span lang=PT-BR style='mso-ansi-language:
