<p class=Normal>Em Savatthi. "Bhikkhus, certa vez no passado um número de videntes virtuosos e com bom caráter haviam se estabelecido em cabanas numa praia à beira do oceano. Agora naquela ocasião os devas e asuras estavam se preparando para a guerra. Então aqueles videntes virtuosos e com bom caráter pensaram: 'Os devas são íntegros, os asuras são corruptos. Os asuras poderão nos trazer perigo. Vamos até Sambara, o senhor dos asuras, para pedir garantias quanto à nossa segurança.'</p>

<p class=Normal>"Então, bhikkhus, com a mesma rapidez com a qual um homem forte pode estender o braço flexionado ou flexionar o braço estendido, aqueles videntes virtuosos e com bom caráter desapareceram das suas cabanas numa praia à beira do oceano e reapareceram na presença de Sambara, o senhor dos asuras. Então aqueles videntes se dirigiram a Sambara em versos: </p>

<p class=Normal style='margin-left:.75in'>
"'Os videntes que vieram até Sambara <br>
pedem por garantias para a sua segurança. <br>

Pois você lhes pode dar aquilo que deseje, <br>
quer seja perigo ou segurança.'</p>

<p class=Normal> [Sambara:] </p>

<p class=Normal style='margin-left:.75in'>
"'Eu não ofereço segurança aos videntes, <br>
pois eles são odiosos discípulos de Sakka; <br>
embora vocês peçam segurança, <br>
eu lhes darei apenas perigo.'</p>

<p class=Normal> [Os videntes:] </p>

<p class=Normal style='margin-left:.75in'>
"'Embora tenhamos pedido segurança, <br>
você apenas nos dá perigo. <br>
Nós recebemos isso das suas mãos: <br>
que você receba perigo interminável! </p>

<p class=Normal style='margin-left:.75in'>
"'Qualquer tipo de semente semeada, <br>
esse é o tipo de fruto que será colhido: <br>
quem faz o bem colhe o bem; <br>
quem faz o mal colhe o mal. <br>
Você, amigo, semeou a semente; <br>
assim experimentará o fruto.'</p>


