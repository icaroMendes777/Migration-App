<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] Os apegos, (<i>upadhi</i>), que também pode ser interpretado como
“aquisições”, denota no seu sentido mais corriqueiro as posses, bagagem, e a parafernália
restante que uma família nômade carrega em suas perambulações, No nível
psicológico, denota tudo aquilo pelo qual alguém possa ter uma noção de “eu” ou
“meu” e que como conseqüência carrega consigo como uma espécie de bagagem
mental. [<a href="#R1">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[2] A palavra “<i>vidito</i>”, compreendido, também significa,
encontrado, descoberto. [<a href="#R2">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[3] <i>Ditthe dhamme</i><span style='mso-bookmark:PVW'></span>:
para ser visto por si mesmo nesta vida ou aqui e agora. É uma expressão
empregada para Nibbana. [<a href="#R3">Retorna</a>] <o:p></o:p></span></p>



<p class=Normal><a name=N4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[4] Nd.II oferece seis distintas válidas interpretações para “acima,
abaixo e no meio”: <o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>- acima = o futuro; abaixo = o passado; e no meio = o presente <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>- acima = o plano dos devas; abaixo = o inferno; e no meio = o plano
humano <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>- acima = habilidade; abaixo = inabilidade; e no meio = qualidades mentais
indeterminadas<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>- acima = a propriedade imaterial; abaixo = a propriedade da sensualidade;
e no meio = a propriedade forma<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>- acima = sensações de prazer; abaixo = sensações de dor; e no meio =
sensações de nem prazer, nem dor <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>- acima = o corpo dos pés para cima; abaixo = o corpo da coroa na cabeça
para baixo; e no meio = o meio do corpo. [<a href="#R4">Retorna</a>]<o:p></o:p></span></p>



