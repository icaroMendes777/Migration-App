<p class=Normal> “Bhikkhus, eu busquei a gratificação nas formas. Qualquer tipo de gratificação que possa haver nas formas - isso eu descobri. Eu vi com clara sabedoria até onde pode chegar a gratificação nas formas. </p>

<p class=Normal> “Bhikkhus, eu busquei o perigo nas formas. Qualquer tipo de perigo que possa haver nas formas - isso eu descobri. Eu vi com clara sabedoria até onde pode chegar o perigo nas formas.</p>

<p class=Normal> “Bhikkhus, eu busquei a escapatória das formas. Qualquer tipo de escapatória das formas que possa haver - isso eu descobri. Eu vi com clara sabedoria até onde pode chegar a escapatória  das formas.</p>

<p class=Normal> “Bhikkhus, eu busquei a gratificação ... o perigo ... a escapatória dos sons ... dos aromas ... dos sabores ... dos tangíveis ... dos objetos mentais. Qualquer tipo de escapatória dos objetos mentais que possa haver - isso eu descobri. Eu vi com clara sabedoria até onde pode chegar a escapatória dos objetos mentais.</p>


<p class=Normal> “Enquanto, bhikkhus, não obtive o conhecimento direto da gratificação, do perigo, e da escapatória dessas seis bases externas, como na verdade elas são, não reivindiquei ter despertado para a insuperável perfeita iluminação neste mundo com os seus devas, maras e brahmas, esta população com seus contemplativos e brâmanes, seus príncipes e povo. Mas quando obtive o conhecimento direto da gratificação, do perigo, e da escapatória dessas seis bases externas, como na verdade elas são, reivindiquei ter despertado para a insuperável perfeita iluminação neste mundo com os seus devas, maras e brahmas, esta população com seus contemplativos e brâmanes, seus príncipes e povo. O conhecimento e visão surgiram em mim: ‘A libertação da minha mente é inabalável. Este é o meu último nascimento. Não há mais vir a ser a nenhum estado.’" </p>

