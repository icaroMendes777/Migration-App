<p class=Normal> “Bhikkhus, Udaka Ramaputta costumava fazer este tipo de afirmação: </p>

<p class=Normal style='margin-left:.75in'>"'Este com certeza é um mestre do conhecimento - <br>
este com certeza é um conquistador universal -<br>
este com certeza removeu<br>
a raiz do tumor que não foi antes removida!'</p>

<p class=Normal>"Bhikkhus, embora Udaka Ramaputta não fosse um mestre do conhecimento, ele afirmava: 'Eu sou um mestre do conhecimento'. Embora ele não fosse um conquistador universal, ele afirmava: 'Eu sou um conquistador universal'. Embora ele não tivesse removido a raiz do tumor, ele afirmava: 'Eu removi a raiz do tumor'. Mas aqui, bhikkhus, um bhikkhu falando corretamente poderia dizer:</p>

<p class=Normal style='margin-left:.75in'>"'Este com certeza é um mestre do conhecimento - <br>
este com certeza é um conquistador universal -<br>
este com certeza removeu<br>
a raiz do tumor que não foi antes removida!'</p>

<p class=Normal>"Como, bhikkhus, alguém é um mestre do conhecimento? Quando um bhikkhu compreende como na verdade é a origem, a cessação, a gratificação, o perigo e a escapatória no caso das seis bases para o contato, esse bhikkhu é um mestre do conhecimento.</p>

<p class=Normal>"Como, bhikkhus, alguém é um conquistador universal? Quando um bhikkhu compreende como na verdade é a origem, a cessação, a gratificação, o perigo e a escapatória no caso das seis bases para o contato, um bhikkhu está libertado através do desapego, esse bhikkhu é um conquistador universal.</p>

<p class=Normal>"Como, bhikkhus, alguém remove a raiz do tumor que não foi antes removida? 'Tumor', bhikkhus, é uma designação para este corpo - consistindo dos quatro grandes elementos - procriado por uma mãe e um pai, construído com arroz cozido e mingau, sujeito à impermanência, a ser gasto e pulverizado, à dissolução e desintegração. A 'raiz do tumor' é uma designação para o desejo. Quando o desejo foi abandonado por um bhikkhu, feito como com um tronco de palmeira, eliminado de modo que não estará mais sujeito a um futuro surgimento, nesse caso o bhikkhu removeu a raiz que não foi antes removida.</p>

<p class=Normal>"Bhikkhus, embora Udaka Ramaputta não fosse um mestre do conhecimento, ele afirmava: 'Eu sou um mestre do conhecimento' ... Mas aqui, bhikkhus, um bhikkhu falando corretamente poderia dizer:</p>

<p class=Normal style='margin-left:.75in'>"'Este com certeza é um mestre do conhecimento - <br>
este com certeza é um conquistador universal -<br>
este com certeza removeu<br>
a raiz do tumor que não foi antes removida!'</p>

