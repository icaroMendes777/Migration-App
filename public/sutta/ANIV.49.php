<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Notas:<o:p></o:p></span></b></p>



<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] As distorções são fundamentais na noção de ignorância ou delusão no
Budismo. Não é que sejamos defeituosos por natureza mas apenas que cometemos
sérios erros em muitos níveis ao tentar entender o mundo à nossa volta. Na
medida em que reconhecemos através da prática da meditação o modo como
interpretamos mal as nossas experiências podemos estar em melhor posição para
corrigir esses erros e obter maior claridade. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>As
distorções operam em três níveis. Primeiro as distorções da percepção (<i
style='mso-bidi-font-style:normal'>sañña-vipallasa</i>) fazem com que
interpretemos mal a informação recebida através das portas dos sentidos. Por
exemplo, podemos confundir uma corda no caminho como sendo uma cobra.
Normalmente esses erros são corrigidos através de uma análise mais cuidadosa,
mas algumas vezes esses erros sensoriais são ignorados e se mantêm. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Distorções
da mente ou do pensamento (<i style='mso-bidi-font-style:normal'>citta-vipallasa</i>)
têm a ver com a etapa seguinte no processo cognitivo quando pensamos ou
ponderamos sobre todo o tipo de coisas. A mente tende a elaborar com base na
percepção e se os pensamentos tiverem por base percepções distorcidas, então
eles também serão distorcidos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em algum
momento esses tipos de pensamentos se tornam habituais e evolvem para
distorções de entendimento (<i style='mso-bidi-font-style:normal'>ditthi-vipallasa</i>).
Podemos estar tão convencidos que existe uma cobra no caminho que não importa a
quantidade de evidências em contrário, a nossa idéia não será modificada. Estamos
aprisionados no entendimento incorreto.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Além disso,
esses três níveis de distorções são cíclicos – nossas percepções se formam no
contexto do nosso entendimento que é reforçado pelos nossos pensamentos e os três
atuam em conjunto para criar os sistemas cognitivos que compõem a nossa
distinta personalidade.[<a href="#R1">Retorna</a>]<o:p></o:p></span></p>



