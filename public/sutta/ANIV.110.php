<p class=Normal> “Bhikkhus, há esses quatro tipos de cobras. Quais quatro? Aquela cujo veneno surge com rapidez mas que não é virulento; aquela cujo veneno não surge com rapidez mas é virulento; aquela cujo veneno ambos surge com rapidez  e é virulento; e aquela cujo veneno não surge com rapidez e não é virulento. Esses são os quatro tipos de cobras. </p>

<p class=Normal> “Da mesma forma, existem quatro tipos de pessoas que podem ser encontradas no mundo que se assemelham a cobras. Quais quatro? Aquela cujo veneno surge com rapidez mas que não é virulento; aquela cujo veneno não surge com rapidez mas é virulento; aquela cujo veneno ambos surge com rapidez  e é virulento; e aquela cujo veneno não surge com rapidez e não é virulento. </p>

<p class=Normal> “E como, bhikkhus, é o tipo de pessoa cujo veneno surge com rapidez mas que não é virulento? É o caso em que alguém fica enraivecido com frequência, mas a sua raiva não permanece por muito tempo. Esse é o tipo de pessoa cujo veneno surge com rapidez mas que não é virulento. Esse tipo de pessoa, lhes digo, é como a cobra cujo veneno surge com rapidez mas que não é virulento. </p>

<p class=Normal> “E como, bhikkhus, é o tipo de pessoa cujo veneno não surge com rapidez mas que  é virulento? É o caso em que alguém não fica enraivecido com frequência, mas a sua raiva permanece por muito tempo. Esse é o tipo de pessoa cujo veneno não surge com rapidez mas que é virulento. Esse tipo de pessoa, lhes digo, é como a cobra cujo veneno não surge com rapidez mas que é virulento. </p>

<p class=Normal> “E como, bhikkhus, é o tipo de pessoa cujo veneno ambos surge com rapidez  e é virulento? É o caso em que alguém fica enraivecido com frequência, e a sua raiva permanece por muito tempo. Esse é o tipo de pessoa cujo veneno ambos surge com rapidez  e é virulento. Esse tipo de pessoa, lhes digo, é como a cobra cujo veneno ambos surge com rapidez  e é virulento. </p>

<p class=Normal> “E como, bhikkhus, é o tipo de pessoa cujo veneno não surge com rapidez e não é virulento? É o caso em que alguém não fica enraivecido com frequência, e a sua raiva não permanece por muito tempo. Esse é o tipo de pessoa cujo veneno não surge com rapidez e não é virulento. Esse tipo de pessoa, lhes digo, é como a cobra cujo veneno não surge com rapidez e não é virulento. </p>

<p class=Normal> “Esses são os quatro tipos de pessoas que podem ser encontradas no mundo." </p>

