<p class=Normal> “Bhikkhus, suponham que um homem ferido e com as feridas supurando se
dirigisse a uma floresta com canas espinhosas, os espinhos picariam os seus pés
e as folhas afiadas como uma lâmina lhe cortariam os membros. E assim ele
experimentaria ainda mais dor e infelicidade. Da mesma maneira, bhikkhus, algum
bhikkhu aqui, dirigindo-se a um vilarejo ou à floresta encontra alguém que o
censura assim: ‘Esse venerável, agindo dessa forma, comportando-se dessa
maneira, é um espinho impuro neste vilarejo’. Tendo compreendido isso desse
modo, como ‘um espinho’, ele deve entender a contenção e a não contenção.<o:p></o:p></span></p>


<p class=Normal> “E como, bhikkhus, ocorre a não contenção? Neste caso, ao ver uma forma
com o olho, um bhikkhu se empenha pela forma prazerosa e repele a forma
des-prazerosa. Ele permanece sem estabelecer a atenção plena no corpo, com a
mente limitada, e não compreende como na verdade é a libertação da mente, a
libertação através da sabedoria, na qual aqueles estados ruins e prejudiciais cessam
sem deixar vestígio. Ao ouvir um som com o ouvido ... Ao conscientizar um objeto
mental com a mente, ele se empenha pelo objeto mental prazeroso e repele o
objeto mental des-prazeroso. Ele permanece sem estabelecer a atenção plena no
corpo, com a mente limitada, e não compreende como na verdade é a libertação da
mente, a libertação através da sabedoria, na qual aqueles estados ruins e
prejudiciais cessam sem deixar vestígio. É desse modo que ocorre a não
contenção.<o:p></o:p></span></p>


<p class=Normal> “Suponham, bhikkhus, que um homem, pegasse seis animais de diferentes
regiões, de diferentes hábitat, e os amarrasse com uma corda forte. Pegando uma
cobra, ele a amarraria com uma corda forte. Pegando um crocodilo ... um pássaro ...
um cachorro ... um chacal ... um macaco, ela o amarraria com uma corda forte.
Amarrando todos com uma corda forte e com um nó no meio ele os soltaria. Então,
cada um desses seis animais de diferentes regiões, de diferentes hábitat,
puxaria na direção da sua região, de seu hábitat. A cobra puxaria pensando ‘Eu
irei para a colina.' O crocodilo puxaria pensando, ‘Eu irei para a água.’ O
pássaro puxaria pensando, ‘Eu voarei para o ar.’ O cachorro puxaria pensando,
‘Eu irei para o vilarejo.’ O chacal puxaria pensando, ‘Eu irei para o
cemitério.’ O macaco puxaria pensando, ‘Eu irei para a floresta.’ <o:p></o:p></span></p>


<p class=Normal> “Agora, quando esses seis animais ficassem exaustos e fatigados, eles
se submeteriam, se renderiam e estariam à mercê daquele que entre eles fosse o
mais forte. Da mesma maneira, bhikkhus, quando um bhikkhu não desenvolveu e
cultivou a atenção plena no corpo, os olhos puxam na direção de formas
prazerosas enquanto que as formas des-prazerosas são repelidas. O ouvido puxa para
sons prazerosos ... O nariz puxa para aromas prazerosos ... A língua puxa para
sabores prazerosos ... O corpo puxa para os tangíveis prazerosos ... A mente puxa
para objetos mentais prazerosos, enquanto que objetos mentais des-prazerosos
são repelidos. <o:p></o:p></span></p>



<p class=Normal> “E como, bhikkhus, ocorre a contenção? Neste caso, ao ver uma forma com
o olho, um bhikkhu não se empenha pela forma prazerosa e não repele a forma
des-prazerosa. Ele permanece com a atenção plena no corpo estabelecida, com a
mente ilimitada, e compreende como na verdade é a libertação da mente, a
libertação através da sabedoria, na qual aqueles estados ruins e prejudiciais cessam
sem deixar vestígio. Ao ouvir um som com o ouvido ... Ao conscientizar um objeto
mental com a mente, ele não se empenha pelo objeto mental prazeroso e não<span
style="mso-spacerun: yes">  </span>repele o objeto mental des-prazeroso. Ele
permanece com a atenção plena no corpo estabelecida, com a mente ilimitada, e
compreende como na verdade é a libertação da mente, a libertação através da
sabedoria, na qual aqueles estados ruins e prejudiciais cessam sem deixar
vestígio. É desse modo que ocorre a contenção.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR>“Como se uma pessoa, pegasse seis animais
de diferentes regiões, de diferentes hábitat, e os amarrasse com uma corda
forte. Pegando uma cobra, ele a amarraria com uma corda forte. Pegando um
crocodilo ... um pássaro ... um cachorro ... um chacal ... um macaco, ele o amarraria
com uma corda forte. Amarrando todos com uma corda forte a um poste sólido.</span></p>


<p class=Normal> “Então, cada um desses seis animais de diferentes regiões, de diferentes
hábitat, puxaria na direção da sua região, de seu hábitat. A cobra puxaria
pensando ‘Eu irei para a colina.’ O crocodilo puxaria pensando, ‘Eu irei para a
água.’ O pássaro puxaria pensando, ‘Eu voarei para o ar.’ O cachorro puxaria
pensando, ‘Eu irei para o vilarejo.’ O chacal puxaria pensando, ‘Eu irei para o
cemitério.’ O macaco puxaria pensando, ‘Eu irei para a floresta.’ E quando
esses seis animais ficassem exaustos eles parariam, sentariam ou deitariam
próximos ao poste. Da mesma maneira, bhikkhus, quando um bhikkhu desenvolveu e
cultivou a atenção plena no corpo, os olhos não puxam na direção de formas
prazerosas enquanto que as formas des-prazerosas não são repelidas. O ouvido
não puxa para sons prazerosos ... O nariz não puxa para aromas prazerosos ... A
língua não puxa para sabores prazerosos ... O corpo não puxa para os tangíveis
prazerosos ... A mente não puxa para objetos mentais prazerosos, enquanto que
objetos mentais des-prazerosos não são repelidos. <o:p></o:p></span></p>


<p class=Normal> “É desta forma, bhikkhus, que ocorre a contenção.<o:p></o:p></span></p>


<p class=Normal> “O ‘poste sólido’: isto, bhikkhus, é um termo utilizado para descrever
a atenção plena no corpo. Portanto, bhikkhus, desse modo é como vocês devem
treinar: ‘Nós desenvolveremos e cultivaremos a atenção plena no corpo, faremos
dela nosso veículo, faremos dela nossa base, estabilizá-la-emos, nela nos
exercitaremos e aperfeiçoá-la-emos.’ Assim é como vocês devem treinar.”<o:p></o:p></span></p>


