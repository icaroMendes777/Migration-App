<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião o Abençoado estava em Kosambi no Parque de Ghosita.<o:p></o:p></span></p>



<p class=Normal><a name=T2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2. Agora, naquela ocasião os bhikkhus de Kosambi estavam envolvidos em
rixas e brigas, mergulhados em discussões, apunhalando uns aos outros usando as
palavras como adagas. Eles não eram capazes, nem de convencer uns aos outros,
nem de serem convencidos por outrem; eles não eram capazes, nem de persuadir
uns aos outros, nem de serem persuadidos por outrem.[<a href="#N1">1</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>3. Então um certo bhikkhu foi até o Abençoado e depois de
cumprimentá-lo, sentou a um lado e relatou o que estava acontecendo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. Então o
Abençoado disse a esse bhikkhu o seguinte: “Venha, bhikkhu, diga a esses
bhikkhus em meu nome que o Mestre os chama.” – “Sim venerável senhor,” ele
respondeu, e foi até aqueles bhikkhus e lhes disse: “O Mestre está chamando os
veneráveis.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
amigo,” eles responderam e foram até o Abençoado, e depois de cumprimentá-lo,
sentaram a um lado. O Abençoado então lhes perguntou: “Bhikkhus, é verdade que
vocês estão envolvidos em rixas e brigas, mergulhados em discussões,
apunhalando uns aos outros usando as palavras como adagas. Que vocês não são
capazes, nem de convencer uns aos outros, nem de serem convencidos por outrem; que
vocês não são capazes, nem de persuadir uns aos outros, nem de serem
persuadidos por outrem?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
venerável senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5.
“Bhikkhus, o que vocês pensam? Quando vocês se envolvem em rixas e brigas,
mergulham em discussões, apunhalam uns aos outros usando as palavras como
adagas, nessas ocasiões vocês estão praticando atos com amor bondade, com o
corpo, linguagem e mente, em público e em particular,<span style="mso-spacerun:
yes">  </span>para com os seus companheiros na vida santa?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
venerável senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
bhikkhus, quando vocês se envolvem em rixas e brigas, mergulham em discussões,
apunhalam uns aos outros usando as palavras como adagas, nessas ocasiões vocês
não estão praticando atos com amor bondade, com o corpo, linguagem e mente, em
público e em particular, para com os seus companheiros na vida santa. Homens
tolos, o que vocês podem quiçá saber, o que vocês podem ver, para se envolverem
em rixas e brigas, mergulharem em discussões, apunhalarem uns aos outros usando
as palavras como adagas? Para não serem capazes, nem de convencer uns aos
outros, nem de serem convencidos por outrem, para não serem capazes, nem de
persuadir uns aos outros, nem de serem persuadidos por outrem? Homens tolos,
isso causará<span style="mso-spacerun: yes">  </span>dano e sofrimento para
vocês por um longo tempo.<o:p></o:p></span></p>



<p class=Normal><a name=T6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>6. Então o Abençoado se dirigiu aos bhikkhus da seguinte forma:
“Bhikkhus, existem essas seis qualidades memoráveis que geram amor e respeito e
que conduzem à coesão, à não disputa, à concórdia e à união. Quais são essas
seis?<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Nesse caso
um bhikkhu pratica, tanto em público como em particular, ações com amor bondade
com o corpo para com os seus companheiros na vida santa. Essa é uma qualidade
memorável que gera amor e respeito e que conduz à coesão, à não disputa, à
concórdia e à unidade.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Outra vez,
um bhikkhu pratica, tanto em público como em particular, ações com amor bondade
com a linguagem para com os seus companheiros na vida santa. Essa é uma
qualidade memorável que gera amor e respeito e que conduz à coesão, à não
disputa, à concórdia e à unidade.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style="mso-spacerun: yes"> </span>“Outra vez, um bhikkhu pratica, tanto em
público como em particular, ações com amor bondade com a mente para com os seus
companheiros na vida santa. Essa é uma qualidade memorável que gera amor e
respeito e que conduz à coesão, à não disputa, à concórdia e à unidade.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Outra vez,
um bhikkhu comparte as coisas com os seus companheiros virtuosos na vida santa;
sem fazer qualquer reserva, ele comparte com eles tudo que seja ganho que
esteja de acordo com o Dhamma e que tenha sido obtido de uma maneira de acordo
com o Dhamma, incluindo até mesmo o conteúdo da sua tigela de alimentos. Essa é
uma qualidade memorável que gera amor e respeito e que conduz à coesão, à não
disputa, à concórdia e à unidade.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Outra vez,
um bhikkhu permanece, tanto em público como em particular, possuindo,
juntamente com os seus companheiros da vida santa, aquelas virtudes que são
inquebrantáveis, que não podem ser despedaçadas, manchadas, matizadas, são
libertadoras, recomendadas pelos sábios, não são mal interpretadas e que
conduzem à concentração. Essa é uma qualidade memorável que gera amor e
respeito e que conduz à coesão, à não disputa, à concórdia e à unidade.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Outra vez,
um bhikkhu permanece, tanto em público como em particular, possuindo,
juntamente com os seus companheiros da vida santa, aquele entendimento que é
nobre, que emancipa, e que conduz aquele que pratica de acordo com ele à
completa destruição do sofrimento. [<a href="#N2">2</a>] <a name=R2></a><span
style="mso-spacerun: yes"> </span>Essa também é uma qualidade memorável que
gera amor e respeito e que conduz à coesão, à não disputa, à concórdia e à
unidade.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Essas são
as seis qualidades memoráveis que geram amor e respeito e que conduzem à coesão, à não disputa, à concórdia e à unidade.<o:p></o:p></span></p>



<p class=Normal><a name=T7></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>7. “Dessas seis qualidades memoráveis, a mais elevada, mais abrangente,
mais conclusiva é o entendimento que é nobre, que emancipa, e que conduz aquele
que pratica de acordo com ele à completa destruição do sofrimento. Da mesma
forma como a parte mais elevada, mais abrangente, mais conclusiva de uma construção
com um pináculo é o próprio pináculo, assim também, dessas seis qualidades
memoráveis, a mais elevada... é o entendimento que é nobre, que emancipa...<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. “E como
esse entendimento que é nobre e que emancipa conduz aquele que pratica de
acordo com ele à completa destruição do sofrimento?<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Nesse caso
um bhikkhu, dirigindo-se à floresta, ou à sombra de uma árvore, ou a um local isolado, considera o seguinte: ‘Existe em mim alguma obsessão que não foi
abandonada e que poderá obcecar a minha mente de tal forma que eu não possa
compreender ou ver as coisas tal como elas na verdade são?’ Se um bhikkhu for
obcecado pelo desejo sensual, então a sua mente estará obcecada. Se um bhikkhu for
obcecado pela má vontade, então a sua mente estará obcecada. Se um bhikkhu
for obcecado pelo torpor e preguiça, então a sua mente estará obcecada. Se um
bhikkhu for obcecado pela inquietação e ansiedade, então a sua mente estará
obcecada. Se um bhikkhu for obcecado pela dúvida, então a sua mente estará
obcecada. Se um bhikkhu estiver absorto em especular sobre este mundo, então a
sua mente estará obcecada. Se um bhikkhu estiver absorto em especular sobre
outro mundo, então a sua mente estará obcecada. Se um bhikkhu estiver envolvido
em rixas e brigas, mergulhado em discussões, apunhalando os outros usando as
palavras como adagas, então a sua mente estará obcecada.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele
compreende da seguinte forma: ‘Não existe em mim obsessão que não tenha sido
abandonada e que poderia obcecar a minha mente de tal forma que eu não pudesse
compreender ou ver as coisas tal como elas na verdade são. A minha mente está
bem preparada para despertar para as verdades.’ [<a href="#N3">3</a>] <a
name=R3></a>Esse é o primeiro conhecimento que ele conquista que é nobre,
supramundano, que não é compartido com as pessoas comuns. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. “Outra
vez, um nobre discípulo considera o seguinte: ‘Quando eu persigo, desenvolvo e
cultivo esse entendimento, eu pessoalmente obtenho paz, eu pessoalmente fico
saciado?’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele
compreende da seguinte forma: ‘Quando eu persigo, desenvolvo e cultivo esse
entendimento, eu pessoalmente obtenho paz, eu pessoalmente fico saciado.’ Esse
é o segundo conhecimento que ele conquista que é nobre, supramundano, que não é
compartido com as pessoas comuns.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. “Outra
vez, um nobre discípulo considera o seguinte: ‘Existe algum outro contemplativo
ou brâmane de fora (do Dhamma do Buda) que possua o entendimento que eu
possuo?’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele
compreende da seguinte forma: ‘Não existe nenhum outro contemplativo ou brâmane
de fora (do Dhamma do Buda) que possua o entendimento que eu possuo.’ Esse é
o terceiro conhecimento que ele conquista que é nobre, supramundano, que não é
compartido com as pessoas comuns.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. “Outra
vez, um nobre discípulo considera o seguinte: ‘Eu possuo o caráter [<a
href="#N4">4</a>] <a name=R4></a>de uma pessoa que possui o entendimento
correto?’ Qual é o caráter de uma pessoa que possui o entendimento correto?
Este é o caráter de uma pessoa que possui o entendimento correto: embora ele
possa cometer algum tipo de ofensa para a qual uma forma de reabilitação foi
definida, [<a href="#N5">5</a>] <a name=R5></a>assim mesmo ele confessa de
imediato, revela e expõe para o Mestre ou para os sábios companheiros da vida
santa, e tendo feito isso ele passa a exercer o autocontrole no futuro. Da
mesma forma como uma criança pequena de imediato retrocede se ela coloca a mão
ou o pé em uma brasa, assim também, esse é o caráter de uma pessoa que possui o
entendimento correto.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele
compreende da seguinte forma: ‘Eu possuo o caráter de uma pessoa que possui o
entendimento correto.’ Esse é o quarto conhecimento que ele conquista que é
nobre, supramundano, que não é compartido com as pessoas comuns.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. “Outra
vez, um nobre discípulo considera o seguinte: ‘Eu possuo o caráter de uma
pessoa que possui o entendimento correto?’ Qual é o caráter de uma pessoa que
possui o entendimento correto? Este é o caráter de uma pessoa que possui o
entendimento correto: embora ele possa estar envolvido em vários assuntos com
os seus companheiros da vida santa, ele no entanto possui um interesse
entusiasmado pelo treinamento na virtude superior, pelo treinamento na mente
superior e pelo treinamento na sabedoria superior. Como uma vaca com o seu
bezerro novo, enquanto ela pasta, observa o seu bezerro, assim também, esse é o
caráter de uma pessoa que possui o entendimento correto.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele
compreende da seguinte forma: ‘Eu possuo o caráter de uma pessoa que possui o
entendimento correto.’ Esse é o quinto conhecimento que ele conquista que é
nobre, supramundano, que não é compartido com as pessoas comuns.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. “Outra
vez, um nobre discípulo considera o seguinte: ‘Eu possuo o poder [<a href="#N6">6</a>]
<a name=R6></a>de uma pessoa que possui o entendimento correto?’ Qual é o poder
de uma pessoa que possui o entendimento correto? Este é o poder de uma pessoa
que possui o entendimento correto: quando o Dhamma e Disciplina, proclamados
pelo Tathagata, estão sendo ensinados, ele ouve, presta atenção, emprega toda a
sua mente, ouve o Dhamma como se tivesse ouvidos ávidos. <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele
compreende da seguinte forma: ‘Eu possuo o poder de uma pessoa que possui o
entendimento correto.’ Esse é o sexto conhecimento que ele conquista que é
nobre, supramundano, que não é compartido com as pessoas comuns.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14. “Outra
vez, um nobre discípulo considera o seguinte: ‘Eu possuo o poder de uma pessoa
que possui o entendimento correto?’ Qual é o poder de uma pessoa que possui o
entendimento correto? Este é o poder de uma pessoa que possui o entendimento
correto: quando o Dhamma e Disciplina, proclamados pelo Tathagata, estão sendo ensinados, 
ele obtém inspiração do significado, ele obtém inspiração do Dhamma,
obtém a satisfação do Dhamma. [<a href="#N7">7</a>]<o:p></o:p></span></p>


<p class=Normal><a name=R7></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Ele compreende da seguinte forma: ‘Eu possuo o poder de uma pessoa que
possui o entendimento correto.’ Esse é o sétimo conhecimento que ele conquista que
é nobre, supramundano, que não é compartido pelas pessoas comuns.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. “Quando
um nobre discípulo possui dessa forma os sete fatores, ele buscou de forma
correta o caráter para a realização do fruto de entrar na correnteza. Quando um
nobre discípulo possui dessa forma os sete fatores, ele possui o fruto de
entrar na correnteza.” [<a href="#N8">8</a>]<o:p></o:p></span></p>


<p class=Normal><a name=R31></a><a name=R8></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>Isso foi o que disse o Abençoado. Os bhikkhus
ficaram satisfeitos e contentes com as palavras do Abençoado. <o:p></o:p></span></p>


