<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim ouvi.
Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no Parque de
Anathapindika. Lá ele se dirigiu aos monges desta forma: “Bhikkhus”. –
“Venerável Senhor,” eles responderam. O Abençoado disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.
“Bhikkhus, um caçador de gamos não coloca um engodo para um rebanho de gamos
intencionando o seguinte: ‘Que o rebanho de gamos possa desfrutar deste engodo
que coloquei e que assim tenha vida longa e formosa e perdure por muito tempo.’
Um caçador de gamos coloca um engodo para um rebanho de gamos intencionando o
seguinte: ‘O rebanho de gamos irá se alimentar de forma imprudente indo
diretamente para o engodo que coloquei; ao fazer isso eles ficarão embriagados;
ao ficarem embriagados, eles se tornarão negligentes; e quando negligentes, eu
poderei fazer deles o que quiser por conta deste engodo.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. “Agora,
os gamos do primeiro rebanho se alimentaram de forma imprudente ao ir
diretamente para o engodo que o caçador de gamos havia colocado; ao fazer isso
eles ficaram embriagados; ao ficarem embriagados, eles se tornaram negligentes;
e quando negligentes, o caçador de gamos fez deles aquilo que ele quis por
conta daquele engodo. Assim é como os gamos do primeiro rebanho fracassaram em
se libertar do poder e controle do caçador de gamos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. “Agora,
os gamos do segundo rebanho pensaram o seguinte: ‘Os gamos do primeiro rebanho,
ao agir como agiram de forma imprudente, fracassaram em se libertar do poder e
controle do caçador de gamos. Suponha que todos nós evitemos aquele engodo;
evitemos<span style='mso-spacerun:yes'>  </span>aquele gozo terrível, vamos
para a floresta para viver lá.’ E assim eles fizeram. Mas no último mês da
estação quente quando o capim e a água haviam sido consumidos, os corpos deles
estavam extremamente emaciados; devido a isso eles perderam a força e a
energia, ao perderem a força e a energia, eles retornaram para aquele mesmo
engodo que o caçador de gamos havia colocado. Eles se alimentaram de forma
imprudente ao ir diretamente para o engodo; ao fazer isso eles ficaram
embriagados; ao ficarem embriagados, eles se tornaram negligentes; e quando
negligentes, o caçador de gamos fez deles aquilo que ele quis por conta daquele
engodo. Assim é como os gamos do segundo rebanho também fracassaram em se
libertar do poder e controle do caçador de gamos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. “Agora
os gamos do terceiro rebanho pensaram o seguinte: ‘Os gamos do primeiro
rebanho, ao agir como agiram de forma imprudente, fracassaram em se libertar do
poder e controle do caçador de gamos. Os gamos do segundo rebanho, avaliando
como os gamos do primeiro rebanho haviam fracassado, e planejando e agindo da
forma como eles fizeram, com a precaução de ir viver na floresta, também
fracassaram em se libertar do poder e controle do caçador de gamos. Suponha que
fizéssemos o nosso refúgio dentro do alcance do engodo do caçador de gamos.
Então, tendo feito isso, nós nos alimentaremos não de forma imprudente sem ir
diretamente para o engodo que o caçador de gamos colocou; ao fazer isso não
ficaremos embriagados; e se não ficarmos embriagados, não nos tornaremos negligentes;
e sem negligência, o caçador de gamos não irá fazer conosco aquilo que ele
quiser por conta daquele engodo.’ E assim eles fizeram.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas,
então, o caçador de gamos e o seus companheiros pensaram o seguinte: ‘Esses
gamos deste terceiro rebanho são astutos e espertos como mágicos e feiticeiros.
Eles comem o engodo que colocamos sem que saibamos como eles vêm e vão. Suponha
que o engodo que colocamos seja completamente cercado numa área ampla com
cercas de galhos trançados; então, talvez possamos descobrir o refúgio do
terceiro rebanho, onde eles vão para se esconder.’ Assim eles fizeram e
descobriram o refúgio do terceiro rebanho, onde eles iam para se esconder.
Assim é como os gamos do terceiro rebanho também fracassaram em se libertar do
poder e controle do caçador de gamos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. “Agora
os gamos do quarto rebanho pensaram o seguinte: ‘Os gamos do primeiro rebanho,
ao agir como agiram de forma imprudente, fracassaram em se libertar do poder e
controle do caçador de gamos. Os gamos do segundo rebanho, avaliando como os
gamos do primeiro rebanho haviam fracassado, e planejando e agindo da forma
como eles fizeram, com a precaução de ir viver na floresta, também fracassaram
em se libertar do poder e controle do caçador de gamos. E os gamos do terceiro
rebanho, avaliando como os gamos do primeiro rebanho e também os gamos do
segundo rebanho fracassaram, e planejando e agindo da forma como eles fizeram
com a precaução de<span style='mso-spacerun:yes'>  </span>fazer o seu refúgio
dentro do alcance do engodo do caçador de gamos, também fracassaram em se
libertar do poder e controle do caçador de gamos. Suponha<span
style='mso-spacerun:yes'>  </span>que fizéssemos o nosso refúgio onde o caçador
de gamos e os seus companheiros não pudessem ir. Então, tendo feito isso, nós
nos alimentaremos não de forma imprudente sem ir diretamente para o engodo
que o caçador de gamos colocou; ao fazer isso não ficaremos embriagados; se não
ficarmos embriagados, não nos tornaremos negligentes; sem negligência, o
caçador de gamos não irá fazer conosco aquilo que ele quiser por conta daquele
engodo.’ E assim eles fizeram.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas então
o caçador de gamos e o seus companheiros pensaram o seguinte: ‘Esses gamos
deste quarto rebanho são astutos e espertos como mágicos e feiticeiros. Eles
comem o engodo que colocamos sem que saibamos como eles vêm e vão. Suponha que
o engodo que colocamos seja completamente cercado numa área ampla com cercas de
galhos trançados; então talvez possamos descobrir o refúgio do quarto rebanho,
onde eles vão para se esconder.’ Assim eles fizeram, mas eles não descobriram o
refúgio do quarto rebanho, onde eles iam para se esconder. Então o caçador de
gamos e os seus companheiros pensaram o seguinte: ‘Se assustarmos o quarto
rebanho, amedrontados eles irão alertar os demais e assim todos os rebanhos de
gamos irão abandonar o engodo que colocamos. Suponha que tratemos o quarto
rebanho de gamos com indiferença.’ E assim eles fizeram. Assim é como os gamos
do quarto rebanho se libertaram do poder e controle do caçador de gamos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7.
“Bhikkhus, eu citei esse símile para transmitir uma idéia. A idéia é a
seguinte: ‘Engodo’ é um termo para os cinco elementos do prazer sensual.
‘Caçador de Gamos’ é um termo para Mara, o Senhor do Mal. ‘Os companheiros do
caçador de gamos’ é um termo para os discípulos de Mara. ‘Rebanho de Gamos’ é
um termo para contemplativos e brâmanes.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. “Agora
os contemplativos e brâmanes do primeiro tipo se alimentaram de forma
imprudente ao ir diretamente para o engodo e para as coisas materiais do mundo
que Mara havia colocado; ao fazer isso eles ficaram embriagados; ao ficarem
embriagados, eles se tornaram negligentes; e quando negligentes, Mara fez deles
aquilo que ele quis por conta daquele engodo e das coisas materiais do mundo.
Assim é como contemplativos e brâmanes do primeiro tipo fracassaram em se
libertar do poder e controle de Mara. Esses contemplativos e brâmanes, eu digo,
<span style='mso-spacerun:yes'> </span>são iguais aos gamos do primeiro
rebanho. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. “Agora,
os contemplativos e brâmanes do segundo tipo pensaram o seguinte: ‘Os
contemplativos e brâmanes daquele primeiro tipo, ao agir como agiram de forma
imprudente, fracassaram em se libertar do poder e controle de Mara. Suponha que
todos nós evitemos aquele engodo e as coisas materiais do mundo; evitemos<span
style='mso-spacerun:yes'>  </span>aquele gozo terrível, vamos para floresta para
viver lá.’ E assim eles fizeram. Lá eles se alimentavam de verduras ou milho,
ou arroz selvagem, ou aparas de peles, ou musgo, ou farelo de arroz, ou a
escuma descartada de arroz cozido, ou farinha de sésamo, ou capim, ou esterco
de vaca, eles viviam de raízes da floresta e frutas, eles se alimentavam de
frutas caídas. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas no
último mês da estação quente quando o capim e a água haviam sido consumidos, os
corpos deles estavam extremamente emaciados; devido a isso eles perderam a
força e a energia, ao perderem a força e a energia, eles perderam a libertação
da mente; [<a href="#N1">1</a>] <a name=R1></a>com a perda da libertação da
mente, eles se alimentaram de forma imprudente ao ir diretamente para o engodo e para as coisas materiais do mundo que Mara havia colocado; ao fazer isso eles ficaram embriagados; ao ficarem
embriagados, eles se tornaram negligentes; e quando negligentes, Mara fez deles
aquilo que ele quis por conta daquele engodo e das coisas materiais do mundo.
Assim é como contemplativos e brâmanes do segundo tipo fracassaram em se
libertar do poder e controle de Mara. Esses contemplativos e brâmanes, eu digo,
são iguais aos gamos do segundo rebanho.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. “Agora,
os contemplativos e brâmanes do terceiro tipo pensaram o seguinte: ‘Aqueles
contemplativos e brâmanes do primeiro tipo, ao agir como agiram de forma
imprudente, fracassaram em se libertar do poder e controle de Mara. Aqueles
contemplativos e brâmanes do segundo tipo, avaliando como os contemplativos e
brâmanes do primeiro tipo haviam fracassado, e planejando e agindo da forma
como eles fizeram, com a precaução de ir viver na floresta, também fracassaram
em se libertar do poder e controle de Mara. Suponha que fizéssemos o nosso
refúgio dentro do alcance daquele engodo que Mara havia colocado e das coisas
materiais do mundo. Então, tendo feito isso, nós nos alimentaremos não de forma
imprudente, sem ir diretamente para o engodo que Mara havia colocado e das
coisas materiais do mundo. Ao fazer isso, não ficaremos embriagados; e se não
ficarmos embriagados, não nos tornaremos negligentes; sem negligência, Mara não
irá fazer conosco aquilo que ele quiser por conta daquele engodo e das coisas
materiais do mundo.’ E assim eles fizeram. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas então eles
passaram a ter idéias como ‘o mundo é eterno’ e ‘o mundo não é eterno’, e ‘o
mundo é finito’ e ‘o mundo não é finito’, e ‘a alma e o corpo são a mesma
coisa’, e ‘a alma é uma coisa e o corpo outra’, e ‘após a morte um Tathagata
existe’ e ‘após a morte um Tathagata não existe’, e ‘após a morte um Tathagata
tanto existe como não existe’, e ‘após a morte um Tathagata nem existe, nem não
existe.’ [<a href="#N2">2</a>] <a name=R2></a>Assim é como contemplativos e
brâmanes do terceiro tipo fracassaram em se libertar do poder e controle de
Mara. Esses contemplativos e brâmanes, eu digo, são iguais aos gamos do
terceiro rebanho.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. “Agora
os contemplativos e brâmanes do quarto tipo pensaram o seguinte: ‘Aqueles
contemplativos e brâmanes do primeiro tipo, ao agir como agiram de forma
imprudente, fracassaram em se libertar do poder e controle de Mara. Aqueles
contemplativos e brâmanes do segundo tipo, avaliando como os contemplativos e
brâmanes do primeiro tipo haviam fracassado, e planejando e agindo da forma
como eles fizeram, com a precaução de ir viver na floresta, também fracassaram
em se libertar do poder e controle de Mara. E os contemplativos e brâmanes do
terceiro tipo, avaliando como os contemplativos e brâmanes do primeiro tipo e
também os contemplativos e brâmanes do segundo tipo haviam fracassado, e
planejando e agindo da forma como eles fizeram, com a precaução de fazer o seu
refúgio dentro do alcance do engodo que Mara havia colocado e das coisas
materiais do mundo, também fracassaram em se libertar do poder e controle de
Mara. Suponha<span style='mso-spacerun:yes'>  </span>que fizéssemos o nosso
refúgio onde Mara e os seus companheiros não pudessem ir. Então, tendo feito
isso, nós nos alimentaremos não de forma imprudente sem ir diretamente para o
engodo que Mara colocou e das coisas materiais do mundo; ao fazer isso não
ficaremos embriagados; se não nos embriagarmos, não nos tornaremos negligentes;
sem negligência, Mara não irá fazer conosco aquilo que ele quiser por conta
daquele engodo e das coisas materiais do mundo.’ E assim eles fizeram. Assim é
como contemplativos e brâmanes do quarto tipo se libertaram do poder e controle
de Mara. Esses contemplativos e brâmanes, eu digo, são iguais aos gamos do
quarto rebanho.<o:p></o:p></span></p>



<p class=Normal><a name=T12></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>12. “E onde é que Mara e os seus companheiros não podem ir? Aqui,
um bhikkhu afastado dos prazeres sensuais, afastado das qualidades não hábeis, entra e permanece no primeiro jhana, que é caracterizado pelo pensamento aplicado e sustentado, com o êxtase e felicidade nascidos do afastamento. Este bhikkhu,
diz-se que vendou os olhos de Mara, se tornou invisível para o Senhor do Mal ao
privar o olho de Mara da sua oportunidade. [<a href="#N3">3</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>13. “Além disso, abandonando o pensamento aplicado e sustentado, um bhikkhu entra e permanece no segundo jhana, que é caracterizado pela segurança interna e perfeita unicidade da mente, sem o pensamento aplicado e sustentado, com o êxtase e felicidade nascidos da concentração. Este bhikkhu, diz-se que vendou
os olhos de Mara ...<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14. “Além
disso, abandonando o êxtase, um bhikkhu entra e permanece no terceiro jhana que é caracterizado pela felicidade sem o êxtase, acompanhada pela atenção plena, plena consciência e equanimidade, acerca do qual os nobres declaram: ‘Ele permanece numa estada feliz, equânime e plenamente atento.’ Este
bhikkhu, diz-se que vendou os olhos de Mara ...<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. “Além
disso, com o completo desaparecimento da felicidade, um bhikkhu entra e permanece no quarto jhana, que possui nem felicidade nem sofrimento, com a atenção plena e a equanimidade purificadas. Este bhikkhu, diz-se que vendou os olhos de Mara ...<o:p></o:p></span></p>



<p class=Normal><a name=T16></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>16. “Além disso, com a completa superação das percepções da forma, com o
desaparecimento das percepções do contato sensorial, sem dar atenção às
percepções da diversidade, consciente de que o ‘espaço é infinito,’ um bhikkhu
entra e permanece na base do espaço infinito. Este bhikkhu, diz-se que vendou
os olhos de Mara...<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17. “Além
disso, com a completa superação da base do espaço infinito, consciente de que a
‘consciência é infinita,’ um bhikkhu entra e permanece na base da consciência
infinita. Este bhikkhu, diz-se que vendou os olhos de Mara ...<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18. “Além
disso, com a completa superação da base da consciência infinita, consciente de
que ‘não há nada,’ um bhikkhu entra e permanece na base do nada. Este bhikkhu,
diz-se que vendou os olhos de Mara ...<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>19. “Além
disso,<span style='mso-spacerun:yes'>  </span>com a completa superação da base
do nada, um bhikkhu entra e permanece na base da nem percepção, nem não
percepção. Este bhikkhu, diz-se que vendou os olhos de Mara...<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>20. “Além
disso, com a completa superação da base da nem percepção, nem não percepção, um
bhikkhu entra e permanece na cessação da percepção e sensação. E as suas
impurezas são destruídas através da visão com sabedoria. Este bhikkhu, diz-se
que vendou os olhos de Mara, se tornou invisível para o Senhor do Mal ao privar
o olho de Mara da sua oportunidade e de ter cruzado para o outro lado do apego
ao mundo.[<a href="#N4">4</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Isso foi o que o Abençoado disse. Os bhikkhus ficaram satisfeitos e
contentes com as palavras do Abençoado.<o:p></o:p></span></p>

