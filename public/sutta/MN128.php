<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim ouvi.
Em certa ocasião o Abençoado estava em Kosambi no Parque de Ghosita.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. Agora,
naquela ocasião os bhikkhus de Kosambi estavam envolvidos em rixas e brigas,
mergulhados em discussões, apunhalando uns aos outros usando as palavras como
adagas.[<a href="#N1">1</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>3. Então um certo bhikkhu foi até o Abençoado e depois de cumprimentá-lo
ficou em pé a um lado e disse: “Venerável senhor, os bhikkhus aqui em Kosambi
estão envolvidos em rixas e brigas, mergulhados em discussões, apunhalando uns
aos outros usando as palavras como adagas. Seria bom, venerável senhor, se o
Abençoado pudesse ir até aqueles bhikkhus por compaixão.” O Abençoado consentiu
em silêncio.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. Então o
Abençoado foi até aqueles bhikkhus e disse: “Basta, bhikkhus, que não haja rixas,
brigas ou disputas.” Quando isso foi dito um certo bhikkhu disse para o
Abençoado: “Espere, venerável senhor! Que o Abençoado, o Senhor do Dhamma, viva
em paz dedicado a uma estada prazerosa aqui e agora. Nós é que somos os
responsáveis por esta rixa, briga e disputa.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Pela
segunda vez ... Pela terceira vez o Abençoado disse: “Basta, bhikkhus, que não
haja rixas, brigas ou disputas.” Pela terceira vez<span
style='mso-spacerun:yes'>  </span>um certo bhikkhu disse para o Abençoado:
“Espere, venerável senhor! Que o Abençoado, o Senhor do Dhamma, viva em paz
dedicado a uma estada prazerosa aqui e agora. Nós é que somos os responsáveis
por esta rixa, briga e disputa.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. Então,
ao amanhecer, o Abençoado se vestiu e tomando a tigela e o manto externo, foi
para Kosambi para esmolar alimentos. Depois de haver esmolado em Kosambi e de
haver retornado, após a refeição, ele arrumou o seu lugar de descanso, tomou a
tigela e o manto externo e enquanto ainda estava em pé proferiu os seguintes
versos:<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'>6. “Quando
muitas vozes gritam ao mesmo tempo <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>ninguém se considera um tolo;<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>embora a Sangha esteja sendo dividida<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>ninguém se considera culpado.</p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Eles esqueceram a linguagem judiciosa,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>eles falam obcecados apenas pelas palavras.<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Com as bocas desenfreadas, eles vociferam de acordo com a sua vontade;<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>ninguém sabe o que os conduz a agir assim.</p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>‘Ele me maltratou, ele me golpeou, [<a href="#N2">2</a>]<br>

<a name=R2></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>ele me derrotou, ele me roubou,’ -<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>para aqueles que abrigam pensamentos como esses<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>a raiva nunca será apaziguada.</p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Pois neste mundo a raiva nunca é<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>apaziguada com outras ações enraivecidas.<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Ela é apaziguada pela não-raiva:<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>essa é a lei imutável e atemporal.</p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Há os que não percebem<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>que nós teremos um fim nesse mundo,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>mas aqueles que percebem isso, <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>têm suas desavenças apaziguadas.</p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Infratores e assassinos,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>aqueles que roubam gado, cavalos, riquezas,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>aqueles que pilham todo o reino -<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>quando até mesmo eles podem agir em conjunto<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>porque vocês não são capazes de fazer isso?</p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Se para a prática você encontrar um amigo <br> 
prudente, virtuoso e sábio,<br>
permaneça com ele com atenção plena e alegria,<br>
superando todas as dificuldades.</p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Se para a prática não for encontrado um amigo<br>
prudente, virtuoso e sábio,<br>
tal como um rei que deixa para trás a terra conquistada,<br>
caminhe só, como um elefante solitário na floresta.</p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Melhor é permanecer só<br>
pois não há companheirismo com os tolos. <br>
Sem fazer o mal, despreocupado,<br>
permaneça só tal como o elefante na floresta.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. Então,
tendo proferido esses versos enquanto ainda estava em pé, o Abençoado foi até o
vilarejo de Balakalonakara. Naquela ocasião o venerável Bhagu estava no
vilarejo de Balakalonakara. Quando o venerável Bhagu o viu chegando à
distância,<span style='mso-spacerun:yes'>  </span>preparou um assento e água
para lavar os pés. O Abençoado sentou no assento que havia sido preparado e
lavou os pés. O venerável Bhagu cumprimentou o Abençoado e sentou a um lado, o
Abençoado disse: “Eu espero que você esteja bem, bhikkhu, eu espero que você
tenha conforto, eu espero que você não esteja enfrentando dificuldades para
obter comida esmolada.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu estou
bem, Abençoado, eu tenho conforto e não tenho enfrentado dificuldades para
obter comida esmolada.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o
Abençoado instruiu, motivou, estimulou e encorajou o venerável Bhagu com um
discurso do Dhamma, depois do que, ele levantou do seu assento e foi para o
Parque do Bambual no Oriente.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. Agora
naquela ocasião o venerável Anuruddha, o venerável Nandiya e o venerável
Kimbila estavam no Parque do Bambual no Oriente.[<a href="#N3">3</a>] <a
name=R3></a>O guarda do Parque viu o Abençoado chegando à distância e disse:
“Não entre neste parque, contemplativo. Aqui há três membros de clãs que estão
buscando o que lhes pode trazer benefício. Não os perturbe.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. O
venerável Anuruddha ouviu o guarda do parque conversando com o Abençoado e
disse: “Amigo guarda, não deixe o Abençoado do lado de fora. Ele é o nosso
Mestre, o Abençoado, que veio.” Então o venerável Anuruddha foi até o venerável
Nandiya e o venerável Kimbila e disse: ‘Venham para fora, veneráveis senhores,
venham para fora! O nosso Mestre, o Abençoado, veio.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. Então
todos os três foram receber o Abençoado. Um tomou a sua tigela e o manto
externo, outro preparou um assento e o outro verteu água para lavar os pés. O
Abençoado sentou no assento que havia sido preparado e lavou os pés. Então aqueles
três veneráveis homenagearam o Abençoado e sentaram a um lado, e o Abençoado
disse: “Eu espero que todos vocês estejam bem, Anuruddha, eu espero que vocês
tenham conforto, eu espero que vocês não estejam enfrentando dificuldades para
obter comida esmolada.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Nós
estamos bem, Abençoado, nós temos conforto e não temos enfrentado dificuldades
para obter comida esmolada.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. “Eu
espero, Anuruddha, que vocês estejam vivendo em concórdia, com respeito mútuo,
sem disputas, combinando como<span style='mso-spacerun:yes'>  </span>leite e
água, considerando um ao outro com bondade.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Com
certeza, venerável senhor, nós estamos vivendo em concórdia, com respeito mútuo,
sem disputas, combinando como leite e água, considerando um ao outro com
bondade.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas,
Anuruddha, como vocês vivem assim?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12.
“Venerável senhor, quanto a isso, eu penso da seguinte forma: ‘É um ganho para
mim, é um grande ganho para mim que eu esteja vivendo a vida santa com estes
companheiros.’ Eu pratico atos com amor bondade com o corpo, em público e em
particular, em relação a esses veneráveis; Eu pratico atos com amor bondade com
a linguagem, em público e em particular, em relação a esses veneráveis; Eu
pratico atos com amor bondade com a mente, em público e em particular, em
relação a esses veneráveis. Eu considero: ‘Porque não deveria deixar de lado
aquilo que quero fazer e fazer aquilo que esses veneráveis querem fazer?’ Então
deixo de lado aquilo que quero fazer e faço aquilo que esses veneráveis querem
fazer. Nós temos corpos distintos, venerável senhor, mas somos únicos na
mente.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O venerável
Nandiya e o venerável Kimbila falaram cada um da mesma forma, adicionando:
“Assim é como, venerável senhor, nós estamos vivendo em concórdia, com respeito
mútuo, sem disputas, combinando como leite e água, considerando um ao outro com
bondade.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. “Muito
bem Anuruddha, eu espero que vocês permaneçam diligentes, ardentes e
decididos.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Com
certeza, venerável senhor, nós permanecemos diligentes, ardentes e decididos.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas,
Anuruddha, como é que vocês assim permanecem?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14.
“Venerável senhor, quanto a isso, qualquer um de nós que primeiro retorne do
vilarejo com comida esmolada prepara os assentos, prepara a água de beber e de
limpeza, e coloca o balde de sobras no seu lugar. Qualquer um de nós que
retorne por último come qualquer comida que tenha sobrado, se ele assim
desejar, de outro modo ele joga aquilo fora onde não haja vegetação ou despeja
na água onde não haja vida. Ele guarda os assentos e a água de beber e de
limpeza. Ele guarda o balde de sobras depois de lavá-lo e varre o refeitório.
Qualquer um que perceber que os jarros com água para beber, água para limpeza ou
água para a latrina estão com o nível baixo ou vazios, toma as providências necessárias.
Se eles forem excessivamente<span style='mso-spacerun:yes'>  </span>pesados,
ele chama alguém através de um sinal com a mão e o outro vem ajudá-lo, mas por
conta disso nós não irrompemos em conversação. E a cada cinco dias nós sentamos
juntos uma noite inteira para discutir o Dhamma. Assim é como permanecemos
diligentes, ardentes e decididos.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. “Muito
bem Anuruddha. Mas enquanto vocês assim permanecem diligentes, ardentes e
decididos, vocês alcançaram algum estado supra-humano, uma distinção no
conhecimento e visão dignos dos nobres, uma estada confortável?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, enquanto assim permanecemos diligentes, ardentes e decididos,
reconhecemos e vemos a luz e as formas. [<a href="#N4">4</a>] <a
name=R4></a>Depois a luz e as formas desaparecem, mas nós
ainda não temos habilidade e compreendemos esses <i>nimittas</i>.” [<a href="#N4A">4A</a>]<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><a
name=R4A></a>16. “Anuruddha, vocês
devem tornar-se proficientes com relação a esses <i>nimittas</i>. [<a href="#N5">5</a>] <a name=R5></a>.
Antes da minha iluminação, quando eu ainda era apenas um Bodisatva não
iluminado, eu também reconhecia e via a luz e as formas.
Depois a luz e as formas desapareciam. Eu pensava: ‘Qual é a causa e
condição pela qual a luz e as formas desaparecem?’ Então considerava o
seguinte: ‘A dúvida surgiu em mim e devido à dúvida a minha concentração
decaiu; quando a minha concentração decaiu, a luz e as formas
desapareceram. Devo agir de tal modo que a dúvida não surja em mim novamente.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17.
“Enquanto, Anuruddha, eu permanecia diligente, ardente e decidido, reconhecia e via a luz e as formas. Depois a luz e a
forma desapareciam. Eu pensava: ‘Qual é a causa e condição pela qual a luz e as formas desaparecem?’ Então considerava o seguinte: ‘A desatenção
surgiu em mim e devido à desatenção a minha concentração decaiu; quando a minha
concentração decaiu, a luz e as formas desapareceram. Devo agir de tal
modo que nem a dúvida, nem a desatenção surjam em mim novamente.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18.
“Enquanto, Anuruddha, eu permanecia diligente, ardente e decidido, reconhecia e via a luz e as formas. Depois a luz e as formas
desapareciam. Eu pensava: ‘Qual é a causa e condição pela qual a luz e a  forma desaparecem?’ Então considerava o seguinte: ‘A preguiça e o torpor
surgiram em mim e devido à preguiça e torpor a minha concentração decaiu;
quando a minha concentração decaiu, a luz e as formas desapareceram.
Devo agir de tal modo que nem a dúvida, nem a desatenção, nem a preguiça e o
torpor surjam em mim novamente.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>19.
“Enquanto, Anuruddha, eu permanecia diligente ... considerava o seguinte: ‘O
medo surgiu em mim, e devido ao medo a minha concentração decaiu; quando a
minha concentração decaiu, a luz e a  forma desapareceram.’ Suponha
que um homem parta para uma jornada e criminosos surjam de ambos os lados;
então o medo surgiria nele devido a isso. Da mesma forma, o medo surgiu em mim
... a luz e as formas desapareceram. [Então considerei o seguinte:]
‘Devo agir de tal modo que nem a dúvida, nem a desatenção, nem a preguiça e o
torpor, nem o medo surjam em mim novamente.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>20.
“Enquanto, Anuruddha, eu permanecia diligente ... considerava o seguinte: ‘A
elação surgiu em mim e devido à elação a minha concentração decaiu; quando a
minha concentração decaiu, a luz e as formas desapareceram.’ Suponha
que um homem procurando pela porta de entrada para um tesouro escondido,
encontre de repente cinco portas de entrada para um tesouro escondido; [<a
href="#N6">6</a>] <a name=R6></a>então a elação surgiria nele devido a isso. Da
mesma forma, a elação surgiu em mim ... a luz e as formas
desapareceram. [Então considerei o seguinte:] ‘Devo agir de tal modo que nem a
dúvida, nem a desatenção ... nem o medo, nem a elação surjam em mim novamente.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>21.
“Enquanto, Anuruddha, eu permanecia diligente ... considerava o seguinte: ‘A
inércia surgiu em mim e devido à inércia a minha concentração decaiu; quando a
minha concentração decaiu, a luz e as formas desapareceram.’ ‘Devo
agir de tal modo que nem a dúvida, nem a desatenção ... nem a elação, nem a
inércia surjam em mim novamente.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>22.
“Enquanto, Anuruddha, eu permanecia diligente ...considerava o seguinte: ‘Uma
energia excessiva surgiu em mim e devido ao excesso de energia a minha
concentração decaiu; quando a minha concentração decaiu, a luz e a 
forma desapareceram.’ Suponha que um homem agarrasse uma codorna com força,
com ambas as mãos, ela morreria ali mesmo naquele instante. Da mesma forma, uma
energia excessiva surgiu em mim ... a luz e as formas desapareceram.
[Então considerei o seguinte:] ‘Devo agir de tal modo que nem a dúvida nem a
desatenção ... nem a inércia, nem a energia excessiva surjam em mim novamente.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>23.
“Enquanto, Anuruddha, eu permanecia diligente ... considerava o seguinte: ‘Uma
energia deficiente surgiu em mim e devido à deficiência de energia a minha
concentração decaiu; quando a minha concentração decaiu, a luz e a 
forma desapareceram.’ Suponha que um homem agarrasse uma codorna frouxamente,
ela sairia voando das mãos dele. Da mesma forma, uma energia deficiente surgiu
em mim ... a luz e as formas desapareceram. [Então considerei o
seguinte:] ‘Devo agir de tal modo que nem a dúvida nem a desatenção ... nem a
energia excessiva nem a energia deficiente surjam em mim novamente.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>24.
“Enquanto, Anuruddha, eu permanecia diligente ... considerava o seguinte: ‘a querença surgiu em mim e devido à querença a minha concentração decaiu; quando a
minha concentração decaiu, a luz e as formas desapareceram.’ ‘Devo
agir de tal modo que nem a dúvida, nem a desatenção ... nem a energia
deficiente, nem a querença surjam em mim novamente.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>25.
“Enquanto, Anuruddha, eu permanecia diligente ... considerava o seguinte: ‘A
percepção da diversidade surgiu em mim e
devido a percepção da diversidade a minha concentração decaiu; quando a minha concentração
decaiu, a luz e as formas desapareceram.’ ‘Devo agir de tal modo que
nem a dúvida, nem a desatenção ... nem a querença, nem a percepção da diversidade
surjam em mim novamente.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>26.
“Enquanto, Anuruddha, eu permanecia diligente ... considerei o seguinte: ‘A<span
style='mso-bidi-font-weight:bold'> reflexão excessiva em relação às formas</span>
surgiu em mim e devido à<span
style='mso-bidi-font-weight:bold'> reflexão excessiva em relação às formas</span>
a minha concentração decaiu; quando a minha concentração decaiu, a luz e a
 forma desapareceram.’ ‘Devo agir de tal modo que nem a dúvida, nem a
desatenção ... nem a percepção da diversidade, nem <span style='mso-bidi-font-weight:
bold'>a reflexão excessiva em relação às formas </span>surjam em mim
novamente.’<o:p></o:p></span></p>



<p class=Normal><a name=T27></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>27. “Quando, Anuruddha, compreendi que a dúvida é uma imperfeição da
mente, [<a href="#N7">7</a>] <a name=R7></a>eu abandonei a dúvida, uma
imperfeição da mente. Quando compreendi que a desatenção ... preguiça e torpor
... medo ... elação ... inércia ... energia excessiva ... energia deficiente
... querença ... percepção da diversidade ... reflexão excessiva em relação às
formas, eu abandonei a reflexão excessiva em relação às formas, uma
imperfeição da mente.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>28.
“Enquanto, Anuruddha, eu permanecia diligente, ardente e decidido, reconhecia a
luz mas não via as formas, via as formas mas não reconhecia a luz, até mesmo
durante uma noite toda ou um dia todo, ou um dia e uma noite toda. Eu pensei:
‘Qual é a causa e condição disso?’ Então considerei da seguinte forma: ‘Na
ocasião em que não dou atenção ao <i>nimitta</i> da forma, mas dou atenção ao <i>nimitta</i> da
luz, então reconheço a luz, mas não vejo as formas. Na ocasião em que não dou
atenção ao <i>nimitta</i> da luz, mas dou atenção ao <i>nimitta</i> da forma, então vejo as formas,
mas não reconheço a luz, até mesmo durante uma noite toda, ou um dia todo, ou um
dia e uma noite toda.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>29.
“Enquanto, Anuruddha, eu permanecia diligente, ardente e decidido, percebia a
luz limitada e via as formas limitadas; eu percebia a luz imensurável e via as formas
imensuráveis, até mesmo durante uma noite toda ou um dia todo, ou um dia e uma
noite toda. Eu pensava: ‘Qual é a causa e condição disso?’ Então considerei da
seguinte forma: ‘Na ocasião em que a concentração é limitada, minha visão é limitada
e com a visão limitada percebo a luz limitada e as formas limitadas. Mas na
ocasião em que a concentração é imensurável, minha visão é imensurável e com a
visão imensurável reconheço a luz imensurável e vejo as formas imensuráveis, até
mesmo durante uma noite toda ou um dia todo, ou um dia e uma noite toda.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>30.
“Quando, Anuruddha, compreendi que a dúvida é uma imperfeição da mente, eu
abandonei a dúvida, uma imperfeição da mente. Quando compreendi que a
desatenção ... preguiça e torpor ... medo ... elação ... inércia ... energia
excessiva ... energia deficiente ... querença ... percepção da diversidade ...
reflexão excessiva em relação às formas, eu abandonei a reflexão excessiva em
relação às formas, uma imperfeição da mente; então pensei: ‘Eu abandonei essas
imperfeições da mente. Agora desenvolverei a concentração de três modos.’ [</span><a
href="#N8"><span lang=PT-BR style='mso-ansi-language:PT-BR'>8</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=R8></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>31. “E assim, Anuruddha, desenvolvi a concentração com o pensamento
aplicado e o pensamento sustentado; desenvolvi a concentração sem pensamento aplicado,
mas apenas com o pensamento sustentado; desenvolvi a concentração sem o
pensamento aplicado e sem o pensamento sustentado; desenvolvi a concentração
com êxtase; desenvolvi a concentração sem êxtase; desenvolvi a concentração
acompanhada pela felicidade; desenvolvi a concentração acompanhada pela
equanimidade. [</span><a href="#N9"><span lang=PT-BR style='mso-ansi-language:
PT-BR'>9</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=R9></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>32. “Quando, Anuruddha, eu desenvolvi a concentração com o pensamento
aplicado e com o pensamento sustentado … quando desenvolvi a concentração
acompanhada pela equanimidade, o conhecimento e visão surgiram em mim: ‘A minha
libertação é inabalável; este é o meu último nascimento; agora não há mais vir
a ser a nenhum estado.’” [<a href="#N10">10</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R10></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Isso foi o que disse o Abençoado. O venerável<span
style='mso-spacerun:yes'>  </span>Anuruddha ficou satisfeito e contente com as
palavras do Abençoado.<o:p></o:p></span></p>

