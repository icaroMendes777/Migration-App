<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(Ambiente)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião o Abençoado estava em Savathi, no Bosque de Jeta, no
Parque de Anathapindika.<o:p></o:p></span></p>



<p class=Normal><a name=T2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2. Agora, naquela ocasião uma idéia perniciosa havia surgido na mente de
um bhikkhu chamado Sati, filho de um pescador: “Da forma como eu entendo, o
Dhamma ensinado pelo Abençoado é a mesma consciência que se move e perambula pelo
ciclo de renascimentos e não uma outra.” [<a href="#N1">1</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. Muitos
bhikkhus, tendo ouvido isso, foram até o bhikkhu Sati e perguntaram: “Amigo
Sati, é verdade que essa idéia perniciosa surgiu na sua mente?<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Exatamente,
amigos. Da forma como eu entendo, o Dhamma ensinado pelo Abençoado é a mesma
consciência que se move e perambula pelo ciclo de renascimentos e não uma outra.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então
aqueles bhikkhus, desejando que ele deixasse de lado aquela idéia perniciosa, o
pressionaram, questionaram e examinaram desta forma: “Amigo Sati, não diga
isto. Não deturpe o Abençoado; não é bom deturpar o Abençoado. O Abençoado não
falaria dessa forma. Pois, em muitos discursos o Abençoado declarou que a
consciência possui origem dependente, já que sem uma condição não existe a
origem da consciência.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Apesar
disso, mesmo tendo sido pressionado, questionado e examinado por eles desta
forma, o bhikkhu Sati, filho de um pescador, ainda assim, obstinadamente<span
style="mso-spacerun: yes">  </span>manteve a sua idéia perniciosa e continuou
insistindo nela.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. Visto que os bhikkhus
foram incapazes de fazer com que ele se separasse dessa idéia perniciosa, eles
se dirigiram até o Abençoado e depois de cumprimentá-lo, sentaram a um lado e
relataram o que havia ocorrido, adicionando: Venerável senhor, visto que fomos
incapazes de fazer com que o bhikkhu Sati,<span style="mso-spacerun: yes"> 
</span>filho de um pescador, se separasse dessa idéia perniciosa, nós estamos
reportando este assunto ao Abençoado.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. Então, o Abençoado se
dirigiu a um certo bhikkhu desta forma: “Venha, bhikkhu, diga em meu nome, ao
bhikkhu Sati, filho de um pescador, que o Mestre o chama.” “Sim, venerável
senhor,” ele respondeu e foi até o bhikkhu Sati e lhe disse: “O Mestre o chama,
amigo Sati.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
Amigo”, ele respondeu, e foi até o Abençoado, após cumprimentá-lo, sentou a um
lado. O Abençoado então lhe perguntou: “Sati, é verdade que a seguinte idéia
perniciosa surgiu em você: ‘Da forma como eu entendo, o Dhamma ensinado pelo
Abençoado é a mesma consciência que se move e perambula pelo ciclo de renascimentos
e não uma outra’?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Exatamente
isso, venerável senhor. Da forma como eu entendo, o Dhamma ensinado pelo
Abençoado é a mesma consciência que se move e perambula pelo ciclo de renascimentos
e não uma outra.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O que é
essa consciência, Sati?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, é aquilo que fala e sente e experimenta aqui e ali o resultado de boas
e más ações.” [<a href="#N2">2</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Homem tolo, para quem você me viu ensinar o Dhamma dessa forma? Homem
tolo, eu não declarei em muitos discursos que a consciência possui origem
dependente, já que sem uma condição não existe a origem da consciência? Mas
você, homem tolo, nos deturpou com o seu entendimento incorreto, causou
prejuízo para si mesmo e acumulou muito demérito; pois isto lhe causará dano e
sofrimento por um longo tempo.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6.
Então, o Abençoado se dirigiu aos bhikkhus desta forma: “Bhikkhus, o que vocês
pensam? Este bhikkhu Sati, filho de um pescador, proporcionou algum lampejo de
sabedoria para este Dhamma e<span style="mso-spacerun: yes"> 
</span>Disciplina?<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Como
poderia ele, venerável senhor? Não, venerável senhor.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isto
foi dito, o bhikkhu Sati, filho de um pescador, permaneceu sentado em silêncio,
consternado, com os ombros caídos e a cabeça baixa, deprimido e sem resposta.
Então, vendo isso, o Abençoado lhe disse: “Homem tolo, você será reconhecido
por sua própria idéia perniciosa. Eu questionarei os bhikkhus sobre este
assunto.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7.
Então, o Abençoado se dirigiu aos bhikkhus desta forma: “Bhikkhus, vocês
entendem o Dhamma que eu ensino da mesma forma que este bhikkhu Sati, filho de
um pescador, entende quando ele nos deturpa com o seu entendimento incorreto e
causa dano para si mesmo e acumula muito demérito?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
venerável senhor. Pois em muitos discursos o Abençoado declarou que a
consciência possui origem dependente, já que sem uma condição não existe a
origem da consciência.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Muito bem,
bhikkhus. É bom que vocês entendam dessa forma o Dhamma que eu ensino. Pois em
muitos discursos eu declarei que a consciência possui origem dependente, já que
sem uma condição não existe a origem da consciência. Mas este bhikkhu Sati,
filho de um pescador, nos deturpa com o seu entendimento incorreto e causa
prejuízo para si mesmo e acumula muito demérito; pois isto levará esse homem
tolo ao dano e sofrimento por um longo tempo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(Condicionalidade
da Consciência)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8.
“Bhikkhus, a consciência é designada pela condição particular de dependência,
dependendo do que ela surge. Quando a consciência surge na dependência do olho
e das formas, ela é designada a consciência no olho; quando a consciência
surge na dependência do ouvido e dos sons, ela é designada a consciência no
ouvido; quando a consciência surge na dependência do nariz e dos aromas, ela é
designada a consciência no nariz; quando a consciência surge na dependência
da língua e dos sabores, ela é designada a consciência na língua; quando a
consciência surge na dependência do corpo e dos tangíveis, ela é designada a
consciência no corpo; quando a consciência surge na dependência da mente e dos
objetos mentais, ela é designada a consciência na mente. Da mesma forma como
o fogo é designado pela condição particular de dependência, dependendo do que
ele arde – quando o fogo arde na dependência de lenha, ele é designado um
fogo de lenha; quando o fogo arde na dependência de gravetos, ele é designado
um fogo de gravetos; quando o fogo arde na dependência de capim, ele é
designado um fogo de capim; quando um fogo arde na dependência de estrume de
vaca ele é designado um fogo de estrume de vaca; quando um fogo arde na
dependência de palha, ele é designado como um fogo de palha; quando um fogo
arde na dependência de lixo, ele é designado um fogo de lixo – assim também a
consciência é designada pela condição particular de dependência, dependendo
do que ela surge.[<a href="#N3">3</a>] <a name=R3></a>Quando a consciência
surge na dependência do olho e das formas, ela é designada a consciência no
olho ... quando a consciência surge na dependência da mente e dos objetos
mentais, ela é designada a consciência na mente.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(Questionário
Geral sobre o Ser/Existir)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9.
“Bhikkhus, vocês vêm: ‘Isto surgiu’?”[<a href="#N4">4</a>] <a name=R4></a>–
“Sim, venerável senhor.” – “Bhikkhus, vocês vêm: ‘A sua origem ocorre tendo
aquilo como alimento’?” – “Sim, venerável senhor.” – “Bhikkhus, vocês vêm: ‘Com
a cessação daquele alimento, aquilo que surgiu está sujeito a cessar’?” – “Sim,
venerável senhor.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10.
“Bhikkhus, a dúvida surge quando existe esta incerteza: ‘Isto surgiu ou não’?”
– “Sim, venerável senhor.” - “Bhikkhus, a dúvida surge quando existe esta
incerteza: ‘A sua origem ocorre tendo aquilo como alimento, ou não’?” - “Sim,
venerável senhor.” - “Bhikkhus, a dúvida surge quando existe esta incerteza:
‘Com a cessação daquele alimento, aquilo que surgiu está sujeito a cessar, ou
não’?” - “Sim, venerável senhor.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11.
“Bhikkhus, a dúvida é abandonada naquele que vê, como na verdade é, com correta
sabedoria, da seguinte forma: ‘Isto surgiu’?” – “Sim, venerável senhor.” –
“Bhikkhus, a dúvida é abandonada naquele que vê, como na verdade é, com correta
sabedoria, da seguinte forma: ‘A sua origem ocorre tendo aquilo como
alimento’?” – “Sim, venerável senhor.” – “Bhikkhus, a dúvida é abandonada
naquele que vê, como na verdade é, com correta sabedoria, da seguinte forma:
‘Com a cessação daquele alimento, aquilo que surgiu está sujeito a cessar’?” –
“Sim, venerável senhor.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12.
“Bhikkhus, vocês estão isentos de dúvidas com relação a isto: ‘Isto surgiu’?” –
“Sim, venerável senhor.” – “Bhikkhus, vocês estão isentos de dúvidas com
relação a isto: ‘A sua origem ocorre tendo aquilo como alimento’?”– “Sim,
venerável senhor.” – “Bhikkhus, vocês estão isentos de dúvidas com relação a
isto: ‘Com a cessação daquele alimento, aquilo que surgiu está sujeito a
cessar’?” – “Sim, venerável senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13.
“Bhikkhus, vocês viram bem, como na verdade é, com correta sabedoria, da
seguinte forma: ‘Isto surgiu’?” – “Sim, venerável senhor.” – “Bhikkhus, vocês
viram bem, como na verdade é, com correta sabedoria, da seguinte forma: ‘A sua
origem ocorre tendo aquilo como alimento’?”– “Sim, venerável senhor.” –
“Bhikkhus, vocês viram bem, como na verdade é, com correta sabedoria, da
seguinte forma: ‘Com a cessação daquele alimento, aquilo que surgiu está
sujeito a cessar’?” – “Sim, venerável senhor.”<o:p></o:p></span></p>



<p class=Normal><a name=T14></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>14. “Bhikkhus, esse entendimento purificado e luminoso, se vocês aderirem
a ele,<span style="mso-spacerun: yes">  </span>amando-o e entesourando-o, e se
o tratarem como se fosse uma posse, vocês então compreenderão o Dhamma que foi
ensinado como semelhante a uma balsa, para ser utilizado para cruzar (a
torrente) e não com o propósito de agarrar-se a ele?” [<a href="#N5">5</a>] <a
name=R5></a>– “Não, venerável senhor.” - “Bhikkhus, esse entendimento purificado 
e luminoso, se vocês não aderirem a ele, não o amarem, não o entesourarem,
e não o tratarem como se fosse uma posse, vocês então compreenderão o Dhamma
que foi ensinado como semelhante a uma balsa, para ser utilizado para cruzar (a
torrente), não com o propósito de agarrar-se a ele?” – “Sim, venerável senhor.”
<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(Alimento e
Origem Dependente)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15.
“Bhikkhus, existem esses quatro tipos de alimentos para a manutenção dos seres
que já nasceram e para o sustento daqueles que estão em busca de um nascimento.
Quais quatro? Eles são: o alimento comida, grosseira ou sutil, o contato como o
segundo, a volição mental como o terceiro e a consciência como o quarto.[<a
href="#N6">6</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>16. “Agora, bhikkhus, o que esses quatro tipos de alimento possuem como
origem, qual é o seu princípio, de que eles nascem e são produzidos? Esses
quatro tipos de alimento possuem o desejo como origem, desejo como o seu
princípio; eles nascem e são produzidos pelo desejo. E esse desejo o que possui
como origem ... ? O desejo possui a sensação como origem ... E essa sensação o que
possui como origem ... ? A sensação possui o contato como origem ... E esse contato
o que possui como origem ... ? O contato possui as seis bases como origem ... E
essas seis bases o que possuem como origem? As seis bases possuem a
mentalidade-materialidade (nome e forma) como origem ... E essa mentalidade-materialidade (nome e forma) o que
possui como origem ... ? A mentalidade-materialidade (nome e forma) possui a consciência como
origem ... E essa consciência o que possui como origem ... ?  A consciência possui
as formações volitivas como origem ... E essas formações volitivas o que possuem como origem, qual é
o seu princípio, de que elas nascem e são produzidas? As formações volitivas possuem a
ignorância como sua origem, ignorância como o seu princípio; elas nascem e são
produzidas pela ignorância.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(Explicação
do Surgimento para Adiante)</b><o:p></o:p></span></p>



<p class=Normal><a name=T17></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>17. “Portanto, bhikkhus, com a ignorância como condição, as formações volitivas
(surgem); com as formações volitivas como condição, a consciência; com a consciência como
condição, a mentalidade-materialidade (nome e forma); com a mentalidade-materialidade (nome e forma) como
condição, as seis bases; com as seis bases como condição, o contato; com o
contato como condição, a sensação; com a sensação como condição, o desejo; com
o desejo como condição, o apego; com o apego como condição, o ser/existir; com
o ser/existir como condição, o nascimento; com o nascimento como condição,
envelhecimento e morte, tristeza, lamentação, dor, angústia e desespero surgem.
Essa é a origem de toda essa massa de sofrimento.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(Questionário
sobre o Surgimento em Ordem Reversa)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style="mso-spacerun: yes"> </span>18. “’Com o nascimento como condição,
envelhecimento e morte’: assim foi dito. Agora, bhikkhus, o envelhecimento e
morte possuem, ou não, o nascimento como condição, vocês aceitam isso?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Envelhecimento
e morte possuem o nascimento como condição, venerável senhor. Isso nós
aceitamos: ’Com o nascimento como condição, envelhecimento e morte.’” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’Com o
ser/existir como condição, nascimento’: assim foi dito. Agora, bhikkhus, o
nascimento possui, ou não, o ser/existir como condição, vocês aceitam isso?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O nascimento
possui o ser/existir como condição, venerável senhor. Isso nós aceitamos: ’Com
o ser/existir como condição, nascimento.’” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’Com o
apego como condição, ser/existir’: assim foi dito. Agora, bhikkhus, o
ser/existir possui, ou não, o apego como condição, vocês aceitam isso?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O
ser/existir possui o apego como condição, venerável senhor. Isso nós aceitamos:
’Com o apego como condição, ser/existir.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’Com o
desejo como condição, apego’: assim foi dito. Agora, bhikkhus, o apego possui,
ou não, o desejo como condição, vocês aceitam isso?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O apego
possui o desejo como condição, venerável senhor. Isso nós aceitamos: ’Com o
desejo como condição, apego.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’Com a
sensação como condição, desejo’: assim foi dito. Agora, bhikkhus, o desejo
possui, ou não, a sensação como condição, vocês aceitam isso?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O desejo
possui a sensação como condição, venerável senhor. Isso nós aceitamos: ’Com a
sensação como condição, desejo.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’Com o
contato como condição, sensação’: assim foi dito. Agora, bhikkhus, a sensação
possui, ou não, o contato como condição, vocês aceitam isso?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“A sensação
possui o contato como condição, venerável senhor. Isso nós aceitamos: ’Com o
contato como condição, sensação.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’Com as seis
bases como condição, contato’: assim foi dito. Agora, bhikkhus, o contato
possui, ou não, as seis bases como condição, vocês aceitam isso?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O contato
possui as seis bases como condição, venerável senhor. Isso nós aceitamos: ’Com
as seis bases como condição, contato.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“’Com a
mentalidade-materialidade (nome e forma) como condição, as seis bases’: assim foi dito. Agora,
bhikkhus, as seis bases possuem, ou
não, a mentalidade-materialidade (nome e forma) como condição, vocês aceitam isso?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“As seis
