<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(O Ciclo de
Existência: da Concepção até a Maturidade)</b><o:p></o:p></span></p>



<p class=Normal><a name=T26></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>26. “Bhikkhus, a concepção de um embrião num ventre ocorre através da
união de três coisas. [<a href="#N9">9</a>] <a name=R9></a>Aqui, existe a união
