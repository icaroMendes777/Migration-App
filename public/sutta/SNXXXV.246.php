<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
se em algum bhikkhu ou bhikkhuni, surgir desejo ou cobiça ou raiva ou delusão
ou aversão em relação às formas conscientizadas através do olho, ele/ela deve
refrear a mente da seguinte forma: ‘Esse caminho é temeroso, perigoso,
espinhoso, cerrado, um caminho depravado, um mal caminho, atormentado pela
escassez. Esse é um caminho trilhado por pessoas inferiores; não é um caminho
trilhado pelas pessoas superiores, não é digno de mim.’ Desse modo a mente deve
ser refreada desses estados em relação às formas vistas pelo olho. Do mesmo
modo com relação aos sons ouvidos pelo ouvido ... aos objetos mentais conscientizados
pela mente.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Suponham,
bhikkhus, que a cevada tivesse amadurecido e o vigia fosse negligente. Um boi
que gosta de cevada, invadindo a plantação para comê-la, se embriagaria tanto
quanto lhe aprouvesse. Da mesma forma, bhikkhus, uma pessoa comum sem
instrução, sem exercer a contenção das seis bases para contato, se embriaga com
os cinco elementos do prazer sensual tanto quanto lhe apraza.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Agora, suponham que a
cevada tivesse amadurecido e o vigia fosse zeloso. O boi que gosta de cevada
invadiria a plantação para comê-la, mas então o vigia o agarraria com firmeza
pela focinheira. Tendo agarrado a foçinheira com firmeza, ele faria com que o
boi se abaixasse empurrando os chifres. Tendo abaixado, ele lhe daria um duro
golpe com uma vara. Tendo dado o duro golpe com uma vara, ele o soltaria. Isso
poderia acontecer uma segunda vez ... uma terceira vez, o boi que gosta de cevada
invadiria a plantação para comê-la, mas então o vigia o agarraria com firmeza
pela focinheira. Tendo agarrado a focinheira com firmeza, ele faria com que o
boi se abaixasse empurrando os chifres. Tendo abaixado, ele lhe daria um duro
golpe com uma vara. Tendo dado o duro golpe com uma vara, ele o soltaria. Como
resultado, o boi que gosta de cevada, não importando se ele fosse para o
vilarejo ou para a floresta, se estivesse em pé ou deitado, não invadiria a
plantação novamente, porque ele se lembraria do sabor da vara. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Da mesma forma, quando a
mente de um bhikkhu é subjugada, bem subjugada, em relação às seis bases para o
contato, ela se estabiliza no interior, se acalma, se<span
style="mso-spacerun: yes">  </span>unifica e se concentra. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Suponham,
bhikkhus, que houvesse um rei ou o ministro de um rei que nunca tivesse
ouvido o som de um alaúde. Ele ouviria o som de um alaúde e diria, ‘O que é,
meu bom homem, esse som tão delicioso, tão sedutor, tão embriagador, tão
arrebatador, tão cativante?’ Ele diria, ‘Isso, senhor, é chamado um alaúde,
cujo som é tão delicioso, tão sedutor, tão embriagador, tão arrebatador, tão
cativante.’ Então o rei diria, ‘Vá e traga-me esse alaúde.’ <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele <a name=PVW>iria</a> e
traria o alaúde dizendo, ‘Aqui, senhor, está o alaúde cujo som é tão delicioso,
tão sedutor, tão embriagador, tão arrebatador, tão cativante.’ O rei diria, ‘Já
basta do seu alaúde. Traga-me apenas o som.’ Ele diria, ‘Este alaúde, senhor, é
feito de muitos componentes, uma grande quantidade de componentes. É através da
ação de muitos componentes que ele soa: isto é, na dependência do corpo, da
casca, do pescoço, da armação, das cordas, da ponte e do esforço humano
apropriado. Assim é como este alaúde, feito de muitos componentes, um grande
número de componentes, soa através da ação de muitos componentes.’ <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então, o rei d<a
name=dismantle>ividiria o al</a>aúde em dez partes, em cem partes. Tendo
partido o alaúde em dez partes, em cem partes, ele o converteria em lascas. Ao
convertê-lo em lascas, ele as queimaria numa fogueira. Depois de queimá-las na
fogueira, ele as reduziria a cinzas. Depois de tê-las reduzido a cinzas, ele as
examinaria sob o efeito de um forte vento ou deixaria que fossem arrastadas por
uma forte correnteza. Então ele diria, ‘Uma coisa triste esse alaúde – seja lá
o que for um alaúde – pelo qual as pessoas têm sido perfeitamente enganadas e
fraudadas.’ <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Da mesma maneira,
bhikkhus, um bhikkhu investiga a forma, até aonde a forma possa ir. Ele investiga
a sensação ... percepção ... formações mentais ... consciência, até aonde a consciência
possa ir. Enquanto ele investiga a forma ... sensação ... percepção ... formações
mentais ... consciência, até aonde a consciência possa ir, quaisquer pensamentos
de ‘eu’ ou ‘meu’ ou ‘eu sou’ que possam ter ocorrido antes, não mais lhe
ocorrem.”<o:p></o:p></span></p>


