<p class=Normal> Em Savatthi. “Bhikkhus, a forma é impermanente. O que é impermanente é sofrimento. O que é sofrimento é não-eu. O que é não-eu deveria ser visto como na verdade é, com correta sabedoria assim: ‘Isso não é meu, isso não sou eu, isso não é o meu eu.’  </p>

<p class=Normal> “A sensação é impermanente ... A percepção é impermanente ... As formações volitivas são impermanentes ... A consciência é impermanente. O que é impermanente é sofrimento. O que é sofrimento é não-eu. O que é não-eu deveria ser visto como na verdade é com correta sabedoria assim: ‘Isso não é meu, isso não sou eu, isso não é o meu eu.’ </p>

<p class=Normal> “Vendo dessa forma, o nobre discípulo bem instruído se desencanta com a forma, se desencanta com a sensação, se desencanta com a percepção, se desencanta com as formações volitivas, se desencanta com a consciência. Desencantado ele se torna desapegado. Através do desapego a sua mente é libertada. Quando ela está libertada surge o conhecimento: ‘Libertada.’ Ele compreende que: ‘O nascimento foi destruído, a vida santa foi vivida, o que deveria ser feito foi feito, não há mais vir a ser a nenhum estado.’” </p>

<p class=Normal><a name=R1></a> “Bhikkhus, qualquer que seja a extensão dos planos de ser/existir, até o pináculo do ser/existir, [<a href="#N1">1</a>] estes são os supremos no mundo, estes são os melhores, isto é, os arahants.’ </p>

<p class=Normal>"Felizes de fato são os arahants! <br>
Nenhum desejo é neles encontrado. <br>
Eliminada a presunção 'eu sou.'<br>
Rompido o véu da delusão. </p>

<p class=Normal>"Eles alcançaram o estado livre da agitação, <br>
suas mentes puras; <br>
no mundo eles são os imaculados -<br>
santos, sem impurezas. </p>

<p class=Normal><a name=R2></a>"Tendo compreendido os cinco agregados por completo, <br>
com as sete boas qualidades, [<a href="#N2">2</a>]<br>
esses homens superiores dignos de elogios<br>
são os filhos do peito do Buda. </p>

<p class=Normal><a name=R3></a>"Dotados com as sete jóias, <br>
treinados no treinamento tríplice, [<a href="#N3">3</a>]<br>
esses grandes heróis perambulam <br>
tendo abandonado o medo e o tremor. </p>

<p class=Normal><a name=R4></a>"Dotados com dez fatores, <br>
esses grandes nagas concentrados, <br>
são os seres supremos no mundo: <br>
nenhum desejo é neles encontrado. [<a href="#N4">4</a>]</p>

<p class=Normal>"O conhecimento de um discípulo neles surgiu: <br>
'Este é meu último corpo,'<br>
Com relação ao núcleo da vida santa<br>
eles não mais dependem de outrem. </p>

<p class=Normal><a name=R5></a>"Eles não vacilam na discriminação, [<a href="#N5">5</a>] <br>
estão libertos do devir. <br>
Tendo alcançado o estado dos domesticados, <br>
eles são os vitoriosos no mundo. </p>

<p class=Normal>"Acima, de lado a lado, abaixo, <br>
o deleite não é mais encontrado neles. <br>
Destemidos soam o rugido do leão: <br>
'Os iluminados são supremos no mundo.'" </p>


