<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'> Ouvi que em certa ocasião o Abençoado estava entre os Sakyas em uma
cidade Sakya denominada Devadaha. Então, um grande grupo de bhikkhus, que iam
na direção de regiões distantes, foram até o Abençoado e ao chegarem o
cumprimentaram, sentaram a um lado e disseram: <o:p></o:p></span></p>


<p class=Normal> 
“Senhor, nós gostaríamos de ir para o campo,
para regiões distantes, e lá estabelecer residência.&quot; <o:p></o:p></span></p>


<p class=Normal>
&quot;Vocês avisaram Sariputta?&quot; <o:p></o:p></span></p>


<p class=Normal>
&quot;Não, senhor, nós não avisamos o Venerável
Sariputta.&quot; <o:p></o:p></span></p>


<p class=Normal>
&quot;Avisem Sariputta, bhikkhus. Sariputta é
sábio, uma grande ajuda para os bhikkhus que são seus companheiros na vida
santa.&quot; <o:p></o:p></span></p>


<p class=Normal>
&quot;Assim faremos, senhor,&quot; os bhikkhus
responderam.<o:p></o:p></span></p>


<p class=Normal>
Nessa ocasião, o Ven. Sariputta estava sentado
sob uma acácia não muito distante do Abençoado. Então os bhikkhus,  satisfeitos e
contentes com as palavras do Abençoado, levantaram dos seus
assentos, e depois de homenagear o Abençoado,<span style="mso-spacerun: yes"> 
</span>mantendo-o à sua direita, dirigiram-se ao Ven. Sariputta. Chegando, eles
o cumprimentaram. Quando a conversa cortês e amigável havia terminado, eles sentaram a um lado e disseram: <o:p></o:p></span></p>


<p class=Normal>
“Amigo Sariputta, nós queremos ir para o campo,
para regiões distantes e lá estabelecer residência. Nós já informamos ao
Mestre.” <o:p></o:p></span></p>


<p class=Normal>
“Amigos, em terras estrangeiras existem nobres
e brâmanes, chefes de família e contemplativos que são sábios e que sabem
diferenciar e que questionarão um bhikkhu: ‘Qual é a doutrina do seu mestre? O
que ele ensina?’ Eu espero que os veneráveis tenham aprendido bem os
ensinamentos, que tenham compreendido, considerado, refletido e penetrado esses
ensinamentos através da sabedoria, de tal maneira que, quando responderem, vocês
falarão de acordo com o que o Abençoado disse, não irão deturpar o Abençoado
com algo contrário aos fatos, responderão de acordo com o Dhamma, de tal modo que
nada que dê margem à censura possa de forma legítima ser deduzido da declaração
de vocês?” <o:p></o:p></span></p>


<p class=Normal>
“Nós viríamos de uma grande distância para
ouvir a explicação dessas palavras do Ven. Sariputta. Seria realmente bom se o
Ven. Sariputta nos esclarecesse isso.”<span style="mso-spacerun: yes">  </span><o:p></o:p></span></p>


<p class=Normal>
&quot;Então, nesse caso, amigos, ouçam e
prestem muita atenção àquilo que eu vou dizer.” -<span style="mso-spacerun:
yes">  </span>“Sim, venerável senhor,” os bhikkhus responderam. O Ven. Sariputta disse o seguinte: <o:p></o:p></span></p>


<p class=Normal>
“Amigos, em terras estrangeiras existem nobres
e brâmanes, chefes de família e contemplativos que são sábios e que sabem
diferenciar e que questionarão um bhikkhu: ‘Qual é a doutrina do seu mestre? O
que ele ensina?’ <o:p></o:p></span></p>


<p class=Normal>
“Assim perguntados vocês devem responder,
‘Nosso mestre ensina a remoção do desejo e cobiça.’ <o:p></o:p></span></p>


<p class=Normal>
“Tendo respondido dessa forma, pode haver
nobres e brâmanes, chefes de família e contemplativos… que questionarão mais,
‘E o seu mestre ensina a remoção do desejo e cobiça pelo que?’ <o:p></o:p></span></p>


<p class=Normal>
“Assim perguntados vocês devem responder,
‘Nosso mestre ensina a remoção do desejo e cobiça pela forma ... pelas
sensações ... pelas percepções ... pelas formações volitivas. Nosso mestre ensina a
remoção do desejo e cobiça pela consciência.’ <o:p></o:p></span></p>


<p class=Normal>
“Tendo respondido dessa forma, pode haver
nobres e brâmanes, chefes de família e contemplativos… que<span
style="mso-spacerun: yes">  </span>questionarão mais, ‘Mas que tipo de<span
style="mso-spacerun: yes">  </span>perigo o seu mestre vê para ensinar a
remoção do desejo e cobiça pela forma ... pelas sensações ... pelas percepções
... pelas formações volitivas.<span style="mso-spacerun: yes">  </span>Que tipo de perigo
o seu mestre vê para<span style="mso-spacerun: yes">  </span>ensinar a remoção
do desejo e cobiça pela consciência?’ <o:p></o:p></span></p>


<p class=Normal>
“Assim perguntados vocês devem responder,
‘Quando alguém não está livre da paixão, desejo, afeição, sede, cobiça e
ambição pela forma, então, por qualquer mudança e alteração nessa forma, surge
a tristeza, lamentação, dor, angústia e desespero. Quando alguém não está livre
da paixão … pelas sensações … pelas percepções … pelas formações volitivas ... Quando
alguém não está livre da paixão, desejo, afeição, sede, cobiça e ambição pela
consciência, então, por qualquer mudança e alteração nessa consciência, surge a
tristeza, lamentação, dor, angústia e desespero. Vendo esse perigo, nosso
mestre ensina a remoção do desejo e cobiça pela forma ... pelas sensações ...
pelas percepções ... pelas fabricações. Vendo esse perigo, nosso mestre ensina
a remoção do desejo e cobiça pela consciência.’ <o:p></o:p></span></p>


<p class=Normal>
“Tendo respondido dessa forma, pode haver
nobres e brâmanes, chefes de família e contemplativos… que questionarão mais,
‘Que beneficio o seu mestre vê para ensinar a remoção do desejo e cobiça pela
forma ... pelas sensações ... pelas percepções ... pelas formações volitivas. Que
benefício o seu mestre<span style="mso-spacerun: yes">  </span>vê para ensinar
a remoção do desejo e cobiça pela consciência?’ <o:p></o:p></span></p>


<p class=Normal>
“Assim perguntados vocês devem responder,
‘Quando alguém está livre da paixão, desejo, afeição, sede, cobiça e ambição
pela forma, então, com qualquer mudança e alteração nessa forma, não surge
nenhuma tristeza, lamentação, dor, angústia, ou desespero. Quando alguém está
livre da paixão ... por sensações ... por percepções … por formações volitivas … Quando
alguém está livre da paixão, desejo, afeição, sede, cobiça e ambição pela
consciência, então com qualquer mudança e alteração nessa consciência, não
surge nenhuma tristeza, lamentação, dor, angústia, ou desespero. Vendo esse
beneficio, nosso mestre ensina a remoção do desejo e cobiça pela forma ...
pelas sensações ... pelas percepções ... pelas formações volitivas. Vendo esse beneficio,
nosso mestre ensina a remoção do desejo e cobiça pela consciência.’ <o:p></o:p></span></p>


<p class=Normal>
“Amigos, se alguém que entra e permanece nos
estados mentais prejudiciais pudesse ter uma permanência agradável no aqui e agora,
sem aflições, desespero e febre, e na desintegração do corpo, após a morte,
pudesse esperar um boa destinação, então o Abençoado não enalteceria o
abandono dos estados mentais prejudiciais. Porém, porque alguém que entra e
permanece nos estados mentais prejudiciais, tem uma permanência desagradável no
aqui e agora, com aflições, desespero e febre, e na dissolução do corpo, após a
morte, pode esperar uma destinação ruim, é que o Abençoado enaltece o abandono
dos estados mentais prejudiciais. <o:p></o:p></span></p>


<p class=Normal>
“Amigos, se alguém que entra e permanece nos
estados mentais benéficos tivesse uma permanência desagradável no aqui e agora,
com aflições, desespero e febre, e na desintegração do corpo, após a morte,
pudesse esperar uma destinação ruim, então o Abençoado não enalteceria o
abandono dos estados mentais prejudiciais. Porém, porque alguém que entra e
permanece nos estados mentais benéficos, tem uma permanência agradável no aqui
e agora, sem aflições, desespero e febre, e na dissolução do corpo, após a
morte, pode esperar uma boa destinação, é<span style="mso-spacerun: yes"> 
</span>que o Abençoado enaltece o abandono dos estados mentais prejudiciais.<o:p></o:p></span></p>


<p class=Normal>
Isso foi o que disse o Ven. Sariputta. Os bhikkhus ficaram
satisfeitos e contentes com as palavras do Ven. Sariputta. <o:p></o:p></span></p>


