<p class=Normal> “Bhikkhus, se errantes membros de outras seitas lhes perguntassem, ‘No que estão enraizados todos os fenômenos? Como eles passam a existir? Qual é a sua origem? Onde eles convergem? Qual é o estado que preside? Qual é o seu princípio governante? Qual é o seu estado transcendente? Qual é o seu cerne? Onde eles se unem? Qual é o seu fim?’: como vocês responderiam?” </p>
<p class=Normal> “Para nós, senhor, os ensinamentos têm o Abençoado como origem, como guia e como refúgio. Seria bom se o Abençoado pudesse explicar o significado dessas palavras. Tendo ouvido do Abençoado, os bhikkhus o recordarão.” </p>
<p class=Normal> “Nesse caso, bhikkhus, ouçam e prestem muita atenção àquilo que eu vou dizer.” – “Sim, venerável senhor,” os bhikkhus responderam. O Abençoado disse o seguinte: </p>
<p class=Normal> “Se errantes membros de outras seitas lhes perguntassem, ‘No que estão enraizados todos os fenômenos? Como eles passam a existir? Qual é a sua origem? Onde eles convergem? Qual é o estado que preside? Qual é o seu princípio governante? Qual é o seu estado transcendente? Qual é o seu cerne? Onde eles se unem? Qual é o seu fim?’ Vocês deveriam responder: </p>

<p class=Normal> “’Todos fenômenos estão enraizados no desejo. [<a href="#N1">1</a>] </p>

<p class=Normal><a name="R1"> </a> “’Todos fenômenos passam a existir através da atenção. </p>

<p class=Normal> “’Todos fenômenos têm o contato como origem. </p>

<p class=Normal> “’Todos fenômenos convergem na sensação. </p>

<p class=Normal> “’Todos fenômenos têm a concentração como estado que preside. </p>

<p class=Normal> “’Todos fenômenos têm a atenção plena como princípio governante. </p>

<p class=Normal> “’Todos fenômenos têm a sabedoria como estado transcendente. </p>

<p class=Normal> “’Todos fenômenos têm a libertação como cerne. </p>

<p class=Normal> “’Todos fenômenos se unem no imortal. </p>

<p class=Normal> “’Todos fenômenos têm nibbana como seu fim. </p>

<p class=Normal> “Se errantes membros de outras seitas lhes perguntarem, assim é como vocês deveriam responder.” </p>

