<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Qualquer
coisa sujeita à desintegração, Ananda, é chamado o mundo na Disciplina dos
Nobres. E o que está sujeito à desintegração? O olho, Ananda, está sujeito à
desintegração, as formas ... a consciência no olho ... o contato no olho ... qualquer
sensação que surja tendo o contato no olho como condição, isso também está
sujeito à desintegração. O ouvido está sujeito à desintegração ... A mente está
sujeita à desintegração, os objetos mentais ... a consciência na mente ... o contato na mente ... qualquer sensação que surja tendo o contato na
mente como condição - experimentada como prazer, dor ou nem prazer, nem dor - isso também
está sujeito à desintegração. Qualquer coisa sujeita à desintegração, Ananda, é
chamado o mundo na Disciplina dos Nobres.” <o:p></o:p></span></p>


