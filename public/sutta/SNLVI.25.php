<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Bhikkhus,
eu digo que a destruição das impurezas é realizada por aquele que sabe e que
vê, [<a href="#N1">1</a>] <a name=R1></a>não por aquele que não sabe e que não
vê. Aquele que sabe o que e aquele que vê o que, realiza a destruição das
impurezas? A destruição das impurezas é realizada por aquele que sabe e que vê:
‘Isto é sofrimento’; por aquele que sabe e que vê: ‘Essa é a origem do
sofrimento’; por aquele que sabe e que vê: ‘Esta é a cessação do sofrimento’;
por aquele que sabe e que vê: ‘Este é o caminho que conduz à cessação do
sofrimento.’ A destruição das impurezas é realizada por aquele que sabe dessa maneira
e que vê dessa maneira.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
bhikkhus, um esforço deve ser feito para compreender: ‘Isto é sofrimento.’ Um
esforço deve ser feito para compreender: ‘Esta é a origem do sofrimento.’ Um
esforço deve ser feito para compreender: ‘Esta é a cessação do sofrimento.’ Um
esforço deve ser feito para compreender: ‘Este é o caminho que conduz à
cessação do sofrimento.’”<o:p></o:p></span></p>

