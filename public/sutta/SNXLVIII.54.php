<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus, assim
como a pegada de qualquer ser vivo que caminha pode ser colocada dentro da
pegada de um elefante e a pegada do elefante ser considerada a líder delas
devido ao seu grande tamanho; do mesmo modo, entre os passos que conduzem à
iluminação, a faculdade da sabedoria é considerada a líder, isto é, para
alcançar a iluminação.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E quais,
bhikkhus, são os passos que conduzem à iluminação? A faculdade da convicção,
bhikkhus, é um passo que conduz à iluminação. A faculdade da energia é um passo
que conduz à iluminação. A faculdade da atenção plena é um passo que conduz à
iluminação. A faculdade da concentração é um passo que conduz à iluminação. A
faculdade da sabedoria é um passo que conduz à iluminação.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Assim como,
bhikkhus, a pegada de qualquer ser vivo que caminha pode ser colocada dentro da
pegada de um elefante<span style='mso-spacerun:yes'>  </span>... assim também,
entre os passos que conduzem à iluminação, a faculdade da sabedoria é considerada
a líder, isto é, para alcançar a iluminação.”<o:p></o:p></span></p>



