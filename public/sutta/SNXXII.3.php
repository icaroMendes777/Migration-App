<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim ouvi.
Em certa ocasião, o ven. Mahakaccana estava em Avanti no monte Papata, em Kuraraghara.
Então, o chefe de família Haliddakani foi até o ven. Mahakaccana e depois de
cumprimentá-lo sentou a um lado e disse: <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, isto foi dito pelo Abençoado no Sutta ‘Perguntas de Magandiya’ (do
Atthaka Vagga) [<a href="#N1">1</a>]:<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'>
<a name=R1></a>‘Tendo abandonado a
casa, perambulando sem morada,<br>

no vilarejo, o sábio não tem
intimidade com ninguém.<br>

Livre dos prazeres sensuais, sem
preferências,<br>

ele não se envolve em disputas com
as pessoas.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Como deve
ser compreendida em detalhe essa breve afirmação do Abençoado?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>[Ven.
Mahakaccana:] “O elemento<span style="mso-spacerun: yes">  </span>forma, chefe
de família, é a casa da consciência. Quando a consciência está aprisionada pela
cobiça ao elemento<span style="mso-spacerun: yes">  </span>forma, diz-se que
ela está vivendo numa casa. O elemento sensação ... percepção ... formações volitivas é a
casa da consciência. Quando a consciência está aprisionada pela<span
style="mso-spacerun: yes">  </span>cobiça ao elemento formações volitivas, diz- se que
ela está vivendo numa casa.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como não
se vive numa casa? Todos os desejos, paixões, deleites, cobiças, apegos,
ligações, idéias, preconceitos ou obsessões em relação ao elemento forma: isso
foi abandonado pelo Tathagata, cortado pela raiz, feito como com um tronco de
palmeira, eliminado de modo que não estará mais sujeito a um futuro surgimento.
Por conseguinte, diz-se que o Tathagata não habita uma casa. Todos os desejos,
paixões, deleites, cobiças, apegos, ligações, idéias, preconceitos ou obsessões
em relação ao elemento sensação ... percepção ... formações volitivas ... consciência: isso foi
abandonado pelo Tathagata, cortado pela raiz, feito como com um tronco de
palmeira, eliminado de modo que não estará mais sujeito a um futuro surgimento.
Por conseguinte, diz-se que o Tathagata não habita uma casa.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como
alguém perambula sem morada? Aquele que está aprisionado às distrações dos
sinais e detalhes da forma, diz-se que ele perambula com uma morada. Aquele que
está aprisionado às distrações dos sinais e detalhes dos sons ... dos aromas
... dos sabores ... dos tangíveis ... dos objetos mentais, diz-se que ele
perambula com uma morada. Assim é como alguém perambula com uma morada. E como alguém perambula sem morada? O Tathagata
abandonou a prisão da distração dos sinais e detalhes da forma, cortou-a pela
raiz, feito como com um tronco de palmeira, eliminada de modo que não estará
mais sujeita a um futuro surgimento. Por conseguinte, diz-se que o Tathagata
perambula sem morada. O Tathagata abandonou a prisão da distração dos sinais e
detalhes dos sons ... dos aromas ... dos sabores ... dos tangíveis ... dos
objetos mentais, cortou-os pela raiz, feitos como com um tronco de palmeira,
eliminados de modo que não estarão mais sujeitos a um futuro surgimento. Por
conseguinte, diz-se que o Tathagata perambula sem morada.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“ E como
alguém tem intimidade no vilarejo? É o caso em que alguém vive enredado com os
chefes de família. Deliciando-se junto deles, entristecendo-se junto deles,
feliz quando eles estão felizes, sentindo dor quando eles sentem dor, ele
assume qualquer assunto de negócios deles como se fosse sua tarefa. Assim é
como alguém tem intimidade no vilarejo. E como alguém não tem intimidade nos
vilarejos? É o caso em que o bhikkhu não vive enredado com os chefes de
família. Ele não se delicia junto deles, não se entristece junto deles, não
está feliz quando eles estão felizes, nem sente dor quando eles sentem dor, ele
não assume qualquer assunto de negócios deles como se fosse sua tarefa. Assim é
como alguém não tem intimidade nos vilarejos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“ E como
alguém não está livre dos prazeres sensuais? É o caso em que alguém não está
livre da paixão, desejo, afeição, sede, cobiça e ambição pelos prazeres
sensuais. Assim é como alguém não está livre dos prazeres sensuais. E como
alguém está livre dos prazeres sensuais? É o caso em que alguém está livre da
paixão, desejo, afeição, sede, cobiça e ambição pelos prazeres sensuais. Assim
é como alguém está livre dos prazeres sensuais.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“ E como
alguém tem preferências? É o caso em que alguém pensa, ‘Que a forma seja assim
no futuro. Que a sensação ... percepção ... formações volitivas ... consciência seja
assim no futuro.’ Assim é como alguém tem preferências.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“ E como
alguém está livre de preferências? É o caso em que alguém não pensa, ‘Que a
forma seja assim no futuro. Que a sensação ... percepção ... formações volitivas ...
consciência seja assim no futuro.’ Assim é como alguém está livre de
preferências.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“ E como
alguém se envolve em disputas com pessoas? É o caso em que alguém fomenta este
tipo de debate: ‘<i>Você </i>entende esta doutrina e disciplina? <i>Eu </i>sou
aquele que entende esta doutrina e disciplina. Como poderia você entender esta
doutrina e disciplina? Você está praticando da forma errada. Eu estou
praticando da forma correta. O que deveria ser dito por último você disse
primeiro. O que deveria ser dito primeiro você disse por último. Eu sou
consistente. Você não é. Aquilo que lhe custou tanto tempo para pensar foi
refutado. A sua doutrina foi derrubada. Você está derrotado. Vá e tente
resgatar a sua doutrina, ou liberte-se se puder!’ Assim é como alguém se
envolve em disputas acaloradas com pessoas.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“ E como alguém
não se envolve em disputas com pessoas? É o caso em que alguém não fomenta este
tipo de debate: ‘<i>Você </i>entende esta doutrina e disciplina? <i>Eu </i>sou
aquele que entende esta doutrina e disciplina. Como poderia você entender esta
doutrina e disciplina? Você está praticando da forma errada. Eu estou
praticando da forma correta. O que deveria ser dito por último você disse
primeiro. O que deveria ser dito primeiro você disse por último. Eu sou
consistente. Você não é. Aquilo que lhe custou tanto tempo para pensar foi
refutado. A sua doutrina foi derrubada. Você está derrotado. Vá e tente
resgatar a sua doutrina ou liberte-se se puder!’ Assim é como alguém não se
envolve em discussões acaloradas com pessoas.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
chefe de família, aquilo que foi dito pelo Abençoado nas ‘Perguntas de
Magandiya’ no Atthaka Vagga:<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'>
‘Tendo abandonado a
casa, perambulando sem morada,<br>

no vilarejo, o sábio não tem
intimidade com ninguém.<br>

Livre dos prazeres sensuais, sem
preferências,<br>

ele não se envolve em disputas com
as pessoas.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“É assim
como deve ser compreendida em detalhe essa breve afirmação do Abençoado.”<o:p></o:p></span></p>


