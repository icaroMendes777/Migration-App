à modéstia; à insatisfação, não à satisfação;<span style="mso-spacerun: yes"> 
</span>ao enredamento, não ao isolamento; à preguiça, não a estimular a
energia; a ser um incômodo, não a não ser um incômodo': Você deve
definitivamente entender, ' Isto não é o Dhamma, isto não é o Vinaya, essas não
são as instruções do Mestre.' <o:p></o:p></span></p>


<p class=Normal> &quot;Quanto às qualidades que você provavelmente conhece. ' Essas
qualidades conduzem ao desapego, não à cobiça; a estar livre dos grilhões, não
a estar agrilhoada; à renúncia, não ao acúmulo; à modéstia, não ao
engrandecimento pessoal; à satisfação, não à insatisfação;<span
style="mso-spacerun: yes">  </span>ao isolamento, não ao enredamento; a estimular
a energia, não à preguiça; a não ser um incômodo, não a ser um incômodo: Você
