<p class=Normal> Então Mahapajapati Gotami foi até o Abençoado e depois de cumprimentá-lo ficou em pé a um lado e
disse: &quot;Seria bom, venerável senhor, se o Abençoado pudesse me ensinar o
Dhamma de maneira resumida de forma que, tendo ouvido o Dhamma do Abençoado, eu
