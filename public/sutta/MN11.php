quatro contemplativos, (<i>samana</i>), mencionados são os quatro graus de nobres
discípulos – que entrou na correnteza, retorna uma vez, não retorna e arahant.
O “rugido do leão”,<span style='mso-spacerun:yes'>  </span>(<i>sihanada</i>), de
acordo com MA, é um rugido de supremacia e destemor, um rugido que não pode ser
refutado. Ligado à proclamação do Buda, veja também a conversa dele com
Subhadda no Mahaparinibbana Sutta (<a href="DN16.php#T5_27">DN 16.5.27</a>). [</span><a
