<p class=Normal><a name=N5></a> [5</span><span lang=PT-BR
style='mso-ansi-language:PT-BR'>] A idéia de ser/existir, (<i>bhavaditthi</i>), é
a imortalidade, a crença em uma vida eterna; a idéia de não ser/existir, (<i>vibhavaditthi</i>),
á a aniquilação, a negação de qualquer princípio de continuidade como base para
o renascimento e a retribuição do kamma. A adoção de uma idéia obriga a
oposição à outra e isto está ligado com a afirmação anterior que o objetivo é
para aquele que não favorece e não opõem. Veja também o </span><a href="It.49.php">Ditthigata Sutta – It.49</a>.
[</span><a href="#R5"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>Retorna</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=N6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[6] Como origem, (<i>samudaya</i>), dessas idéias, MA menciona oito
condições: os cinco agregados, ignorância, contato, percepção, pensamentos,
atenção sem sabedoria, amizades prejudiciais e a voz de um outro. A cessação delas, (<i>atthangama</i>),
é o caminho de entrar na correnteza, que erradica todas as idéias incorretas. A
sua gratificação, (<i>assada</i>), pode ser compreendida como a satisfação que
elas proporcionam para as necessidades psicológicas; o seu perigo, (<i>adinava</i>),
é o contínuo aprisionamento que elas exigem; a escapatória, (<i>nissarana</i>),
delas é Nibbana. [</span><a href="#R6"><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Retorna</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=N7></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[7] MA aponta neste caso a completa compreensão, (<i>pariñña</i>), como
superar, transcender, (<i>samatikkama</i>), referindo-se à noção<span
style='mso-spacerun:yes'>  </span><i>pahanapariñña</i> dos comentários,
‘completa compreensão como abandono (abandono do desejo e cobiça).’ Veja o <a
href="MN1.php#N7">MN 1, nota 7</a>. [</span><a href="#R7"><span lang=PT-BR
style='mso-ansi-language:PT-BR'>Retorna</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=N8></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[8] Este trecho afirma com clareza que o fator crítico que diferencia os
ensinamentos do Buda dos demais credos religiosos e filosóficos é a sua
“completa compreensão do apego à doutrina de um eu.” Isso
significa, de fato, que apenas o Buda é capaz de mostrar como superar todas as
idéias da existência de um eu através da penetração da verdade do não-eu. Como
aos demais mestres espirituais lhes falta essa compreensão do não-eu, as suas
afirmativas de compreender de forma completa os outros três tipos de apego
também fica sob suspeita. [</span><a href="#R8"><span lang=PT-BR
style='mso-ansi-language:PT-BR'>Retorna</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=N9></a><span lang=PT-BR style='mso-ansi-language:
