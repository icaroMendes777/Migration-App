<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no
Parque de Anathapindika. Lá ele se dirigiu aos monges desta forma: “Bhikkhus.”
– “Venerável senhor,” eles responderam. O Abençoado disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><a name=T2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2. “Bhikkhus, somente aqui há um contemplativo, somente aqui há um
segundo contemplativo, somente aqui há um terceiro contemplativo, somente aqui
há um quarto contemplativo. As doutrinas dos outros estão desprovidas de
contemplativos: assim é como vocês deveriam com justiça rugir o seu rugido de
leão. [</span><a href="#N1"><span lang=PT-BR style='mso-ansi-language:PT-BR'>1</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>3. “É possível, bhikkhus, que errantes de outras seitas possam
perguntar: ‘Mas com base em qual argumento ou apoiado em qual autoridade os
veneráveis dizem isso?’ Os errantes de outras seitas que assim perguntarem
devem ser respondidos da seguinte forma: ‘Amigos, quatro coisas nos foram
declaradas pelo Abençoado que sabe e vê, o arahant, perfeitamente iluminado; ao
vê-las por nós mesmos nós dizemos isto: “Somente aqui há um contemplativo,
somente aqui há um segundo contemplativo, somente aqui há um terceiro
contemplativo, somente aqui há um quarto contemplativo. As doutrinas dos outros
estão desprovidas de contemplativos.” Quais quatro? Nós temos confiança no
Mestre, nós temos confiança no Dhamma, nós cumprimos os preceitos e os nossos
companheiros no Dhamma<span style='mso-spacerun:yes'>  </span>são estimados e
amados por nós quer sejam discípulos leigos ou na vida santa. Essas são as
quatro coisas que nos foram declaradas pelo Abençoado que sabe e vê, o arahant, perfeitamente iluminado, e dizemos isso ao vê-las por nós mesmos .’ <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. “É
possível, bhikkhus, que errantes de outras seitas possam dizer o seguinte:
‘Amigos, nós também temos confiança no Mestre, isto é, nosso Mestre; nós também
temos confiança no Dhamma, isto é, nosso Dhamma; nós também cumprimos os
preceitos, isto é, os nossos preceitos; e os nossos companheiros no Dhamma<span
style='mso-spacerun:yes'>  </span>são estimados e amados por nós quer sejam
discípulos leigos ou na vida santa. Qual é a distinção, amigos, qual é a
variação, qual é a diferença entre você e nós?’<o:p></o:p></span></p>



<p class=Normal><a name=T5></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>5. “Os errantes de outras seitas que assim perguntarem devem ser
respondidos da seguinte forma: ‘Como então, amigos, o objetivo é um só ou são
muitos?’ Respondendo da forma correta, os errantes de outras seitas
responderiam o seguinte: ‘Amigos, o objetivo é um só, não muitos.’ [</span><a
href="#N2"><span lang=PT-BR style='mso-ansi-language:PT-BR'>2</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R2></a>– ‘Mas, amigos,
esse objetivo é para alguém influenciado pela cobiça ou para alguém livre de
cobiça?’ Respondendo da forma correta, os errantes de outras seitas
responderiam o seguinte: ‘Amigos, esse objetivo é para alguém livre da cobiça,
não para alguém influenciado pela cobiça.’ – ‘Mas, amigos, esse objetivo é para
alguém influenciado pela raiva ou para alguém livre da raiva?’ Respondendo da
forma correta, os errantes de outras seitas responderiam o seguinte: ‘Amigos,<span
style='mso-spacerun:yes'>  </span>esse objetivo é para alguém livre da raiva,
não para alguém influenciado pela raiva.’ – ‘ Mas, amigos, esse objetivo é para
alguém influenciado pela delusão ou para alguém livre da delusão?’ Respondendo
da forma correta, os errantes de outras seitas responderiam o seguinte:
‘Amigos, esse objetivo é para alguém livre da delusão, não para alguém
influenciado pela delusão.’- ‘Mas, amigos, esse objetivo é para alguém
influenciado pelo desejo ou para alguém livre do desejo?’ Respondendo da forma
correta, os errantes de outras seitas responderiam o seguinte: ‘Amigos, esse
objetivo é para alguém livre do desejo, não para alguém influenciado pelo
desejo.’- ‘Mas, amigos, esse objetivo é para alguém influenciado pelo apego ou
para alguém livre do apego?’ Respondendo da forma correta, os errantes de
outras seitas responderiam o seguinte: ‘Amigos, esse objetivo é para alguém
livre do apego, não para alguém influenciado pelo apego.’- ‘Mas, amigos, esse
objetivo é para alguém que tem visão ou para alguém que não tem visão?’ Respondendo
da forma correta, os errantes de outras seitas responderiam o seguinte:
‘Amigos, esse objetivo é para alguém que tem visão, não para alguém que não tem
visão.’ - ‘Mas, amigos, esse objetivo é para alguém que favorece e opõe ou para
alguém que não favorece e opõe?’ Respondendo da forma correta, os errantes de
outras seitas responderiam o seguinte: ‘Amigos, esse objetivo é para alguém que
não favorece e opõe, não para alguém que favorece e opõe.’[</span><a href="#N3"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>3</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>] – <a name=R3></a>‘Mas, amigos, esse objetivo
é para alguém que se delicia e desfruta com a proliferação ou para alguém que
não se delicia e desfruta com a proliferação?’ Respondendo da forma correta, os
errantes de outras seitas responderiam o seguinte: ‘Amigos, esse objetivo é
para alguém que não se delicia e desfruta com a proliferação, não para alguém
que se delicia e desfruta com a proliferação.’ [</span><a href="#N4"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>4</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=R4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>6. “Bhikkhus, existem essas duas idéias: a idéia de ser/existir e a
idéia de não ser/existir. Todos contemplativos ou brâmanes que confiam na idéia
de ser/existir, adotam a idéia de ser/existir, aceitam a idéia de ser/existir,
se opõem à idéia de não ser/existir. Todos contemplativos e brâmanes que
confiam na idéia de não ser/existir, adotam a idéia de não ser/existir, aceitam
a idéia de não ser/existir, se opõem à idéia de ser/existir. [</span><a
href="#N5"><span lang=PT-BR style='mso-ansi-language:PT-BR'>5</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=R5></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>7. “Todos contemplativos ou brâmanes que não compreendem como na verdade
é a origem, a cessação, a gratificação, o perigo e a escapatória [</span><a
href="#N6"><span lang=PT-BR style='mso-ansi-language:PT-BR'>6</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R6></a>com relação a essas
duas idéias, estão influenciados pela cobiça, influenciados pela raiva,
influenciados pela delusão, influenciados pelo desejo, sem visão, dados ao
favorecimento e oposição, e eles se deliciam e desfrutam com a proliferação.
Eles não estão livres do nascimento, envelhecimento e morte; da tristeza,
lamentação, dor, angústia e desespero; eles não estão livres do sofrimento, eu
digo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. “Todos
os contemplativos ou brâmanes que compreendem como na verdade é a origem, a
cessação, a gratificação, o perigo e a escapatória com relação a essas duas
idéias, estão livres da cobiça, livres da raiva, livres da delusão, livres do
desejo, com visão, não dados ao favorecimento e oposição e eles não se deliciam
e desfrutam com a proliferação. Eles estão livres do nascimento, envelhecimento
e morte; da tristeza, lamentação, dor, angústia e desespero; eles estão livres
do sofrimento, eu digo.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9.
“Bhikkhus, existem esses quatro tipos de apego. Quais quatro? Apego a prazeres
sensuais, apego a idéias, apego a preceitos e rituais e apego à doutrina de um eu.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. “Embora
certos contemplativos e brâmanes reivindiquem apresentar a completa compreensão
de todos os tipos de apego, eles não descrevem de modo completo a completa
compreensão de todos os tipos de apego.[</span><a href="#N7"><span lang=PT-BR
style='mso-ansi-language:PT-BR'>7</span></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>] <a name=R7></a>Eles descrevem apenas a completa compreensão do apego a
prazeres sensuais sem descrever a completa compreensão do apego a idéias, do apego
a preceitos e rituais e do apego à doutrina de um eu. Por que
isso? Eles não compreendem essas três
instâncias de apego como elas na verdade ocorrem. E assim, embora reivindiquem
apresentar a completa compreensão de todos os tipos de apego, eles descrevem
apenas a completa compreensão do apego a prazeres sensuais sem descrever a
completa compreensão do apego a idéias, do apego a preceitos e rituais e do
apego à doutrina de um eu.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. “Embora
certos contemplativos e brâmanes reivindiquem apresentar a completa compreensão
de todos os tipos de apego ... eles descrevem apenas a completa compreensão do
apego a prazeres sensuais<span style='mso-spacerun:yes'>  </span>e do apego a
idéias sem descrever a completa compreensão do apego a preceitos e rituais e do
apego à doutrina de um eu. Por que isso? Eles não compreendem
duas instâncias ... E assim eles descrevem apenas a completa compreensão do
apego a prazeres sensuais e do apego a idéias sem descrever a completa
compreensão do apego a preceitos e rituais e do apego à doutrina de um eu.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. “Embora
certos contemplativos e brâmanes reivindiquem apresentar a completa compreensão
de todos os tipos de apego ... eles descrevem apenas a completa compreensão do
apego a prazeres sensuais, do apego a idéias e do apego a preceitos e rituais
sem descrever a completa compreensão do apego à doutrina de um eu. Eles não compreendem uma instância ... 
E assim eles descrevem apenas a
completa compreensão do apego a prazeres sensuais, do apego a idéias e do apego
a preceitos e rituais sem descrever a completa compreensão do apego à doutrina de um eu.[</span><a href="#N8"><span lang=PT-BR
style='mso-ansi-language:PT-BR'>8</span></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=R8></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>13. “Bhikkhus, em um Dhamma e Disciplina como esse, é claro que a
confiança no Mestre não está direcionada da forma correta, que a confiança no
Dhamma não está direcionada da forma correta, que o cumprimento dos preceitos
não está direcionado da forma correta e de que a estima entre os companheiros
no Dhamma não está direcionada da forma correta. Por que isso? Porque assim é
quando o Dhamma e a Disciplina são mal proclamados e mal explicados, quando não
conduzem à emancipação, não conduzem à paz, expostos por alguém que não é
perfeitamente iluminado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14. “Bhikkhus,
quando um Tathagata, um arahant, perfeitamente iluminado, reivindica apresentar a
completa compreensão de todos os tipos de apego, ele descreve completamente a
completa compreensão de todos os tipos de apego: ele descreve a completa
compreensão do apego a prazeres sensuais, do apego a idéias, do apego a
preceitos e rituais e do apego à doutrina de um eu.[</span><a
href="#N9"><span lang=PT-BR style='mso-ansi-language:PT-BR'>9</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=R9></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>15. “Bhikkhus, em um Dhamma e Disciplina como esse, é claro que a
confiança no Mestre está direcionada da forma correta, que a confiança no
Dhamma está direcionada da forma correta, que o cumprimento dos preceitos está
direcionado da forma correta e de que a estima entre os companheiros no Dhamma
está direcionada da forma correta. Por que isso? Porque assim é quando o Dhamma
e a Disciplina são bem proclamados e bem explicados, quando conduzem à
emancipação, conduzem à paz, expostos por alguém que é perfeitamente iluminado.<o:p></o:p></span></p>



<p class=Normal><a name=T16></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>16. “Agora esses quatro tipos de apego possuem o que como fonte, o que
como origem, do que nascem e são produzidos? Esses quatro tipos de apego
possuem o desejo como fonte, desejo como origem, eles nascem e são produzidos
do desejo. [</span><a href="#N10"><span lang=PT-BR style='mso-ansi-language:
PT-BR'>10</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>] <a
name=R10></a>E o desejo possui o que como fonte … ? Desejo possui a sensação como
fonte … A sensação possui o que como fonte … ? A sensação possui o contato como
fonte … O contato possui o que como fonte … ? O contato possui as seis bases como
fonte … As seis bases possuem o que como fonte … ? As seis bases possuem a
mentalidade-materialidade (nome e forma) como fonte … A mentalidade-materialidade (nome e forma) possui o que
como fonte … ? A mentalidade-materialidade (nome e forma) possui a consciência como fonte … A
consciência possui o que como fonte … ? A consciência possui as formações volitivas como
fonte … As formações volitivas possuem o que como fonte … ? As formações possuem a ignorância
como fonte, ignorância como origem, elas nascem e são produzidas da ignorância.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17.
“Bhikkhus, quando a ignorância é abandonada e o verdadeiro conhecimento surgiu
num bhikkhu, então com a cessação da ignorância e o surgimento do verdadeiro conhecimento
ele não mais se apega aos prazeres sensuais, não mais se apega a idéias, não
mais se apega a preceitos e rituais, não mais se apega à doutrina de um eu. [</span><a href="#N11"><span lang=PT-BR style='mso-ansi-language:
PT-BR'>11</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>] <a
name=R11></a>Não se apegando, ele não fica agitado. Sem estar agitado, ele
realiza Nibbana. Ele compreende que: ‘O nascimento foi destruído, a vida santa
foi vivida, o que deveria ser feito foi feito, não há mais vir a ser a nenhum
estado.’”<o:p></o:p></span></p>



<p class=Normal><a name=R31></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Isso foi o que disse o Abençoado. Os bhikkhus ficaram satisfeitos e
contentes com as palavras do Abençoado. <o:p></o:p></span></p>

