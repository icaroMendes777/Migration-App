<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[2] MA: Muito embora todos os adeptos de outras seitas declarem o estado
de arahant como
sendo o objetivo – compreendido de uma forma geral como a perfeição espiritual – eles indicam realizações diferentes 
como sendo o objetivo, de
acordo com as suas idéias. Assim os brâmanes declaram o mundo de Brahma como
sendo o objetivo, os ascetas declaram os devas da Radiância, os errantes declaram os
devas da Glória Refulgente e os Ajivakas declaram o estado da não percepção, que eles
imaginam ser a “mente infinita.” [</span><a href="#R2"><span lang=PT-BR
