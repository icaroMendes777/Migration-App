<p class=Normal>"Bhikkhus, possuindo cinco qualidades um bhikkhu permanece sofrendo nesta mesma vida - com aflição, angústia e febre - e com a dissolução do corpo, após a morte, ele pode esperar um destino ruim. Quais cinco? Aqui, um bhikkhu está desprovido de convicção, de vergonha de cometer transgressões, de temor de cometer transgressões, preguiçoso, um tolo. Possuindo essas cinco qualidades um bhikkhu permanece sofrendo nesta mesma vida - com aflição, angústia e febre - e com a dissolução do corpo, após a morte, ele pode esperar um destino ruim.</p>

