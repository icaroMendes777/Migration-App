<p class=Normal style='margin-left:.75in'>

 

<p class=Normal> “Bhikkhus, quando uma cidade real fortificada está bem provida com os sete requisitos de uma fortificação e pode obter de acordo com a sua vontade, sem problemas e sem dificuldades, os quatro tipos de alimento, então é dito que ela é uma cidade real fortificada que não pode ser destruída por inimigos externos ou aliados falazes. </p>

 

<p class=Normal> “E quais são os sete requisitos com o qual uma fortificação está  bem provida? </p>

 

<p class=Normal> “É o caso em que uma cidade real fortificada tem pilastras como fundação  – profundamente enraizadas, bem plantadas, inamovíveis e inabaláveis. Com esse primeiro requisito uma fortificação está bem provida para a proteção daqueles que estão dentro e para repelir aqueles que estão fora. </p>

 

<p class=Normal> “Além disso, uma cidade real fortificada tem um fosso, tanto profundo como largo.  Com esse segundo requisito uma fortificação está bem provida para a proteção daqueles que estão dentro e para repelir aqueles que estão fora. </p>

 

<p class=Normal> “Além disso, uma cidade real fortificada tem uma plataforma que a circunda, tanto alta como larga. Com esse terceiro requisito uma fortificação está bem provida para a proteção daqueles que estão dentro e para repelir aqueles que estão fora. </p>

 

<p class=Normal> “Além disso, uma cidade real fortificada tem muitas armas armazenadas, tanto flechas como coisas para serem arremessadas. Com esse quarto requisito uma fortificação está bem provida para a proteção daqueles que estão dentro e para repelir aqueles que estão fora. </p>

 

<p class=Normal> “Além disso, uma cidade real fortificada tem um grande exército aquartelado no seu interior – elefantes, cavalaria, cocheiros, arqueiros, porta-estandartes, oficiais de alojamento, soldados da logística, príncipes conhecidos, heróis comandantes, infantaria e escravos. Com esse quinto requisito uma fortificação está bem provida para a proteção daqueles que estão dentro e para repelir aqueles que estão fora. </p>

 

<p class=Normal> “Além disso, uma cidade real fortificada tem um guardião dos portões - sábio, competente e inteligente - que não permite a entrada de desconhecidos e admite que entrem os conhecidos. Com esse sexto requisito uma fortificação está bem provida para a proteção daqueles que estão dentro e para repelir aqueles que estão fora. </p>

 

<p class=Normal> “Além disso, uma cidade real fortificada tem muralhas: altas e espessas e completamente revestidas com argamassa. Com esse sétimo requisito uma fortificação está bem provida para a proteção daqueles que estão dentro e para repelir aqueles que estão fora. </p>

 

<p class=Normal> “Esses são os sete requisitos de uma cidade real fortificada que está bem provida. </p>

 

<p class=Normal> “E quais são os quatro tipos de alimento que podem ser obtidos de acordo com a sua vontade, sem problemas e sem dificuldades? </p>

 

<p class=Normal> “É o caso em que uma cidade real fortificada tem um grande estoque de capim, madeira e água para o prazer, comodidade e conforto daqueles que estão dentro e para repelir aqueles que estão fora. Além disso, ela tem grandes suprimentos de arroz e cevada para o prazer, comodidade e conforto daqueles que estão dentro e para repelir aqueles que estão fora. Além disso, ela tem grandes suprimentos de gergelim, grão de bico e vários tipos de feijões para o prazer, comodidade e conforto daqueles que estão dentro e para repelir aqueles que estão fora. Além disso, ela tem grandes suprimentos de tônicos – manteiga líquida, manteiga fresca, azeite, mel, melaço e sal - para o prazer, comodidade e conforto daqueles que estão dentro e para repelir aqueles que estão fora. Esses são os quatro tipos de alimento que  podem ser obtidos de acordo com a sua vontade, sem problemas e sem dificuldades. </p>

 

<p class=Normal> “Quando uma cidade real fortificada está bem provida com os sete requisitos de uma fortificação e pode obter de acordo com a sua vontade, sem problemas e sem dificuldades, os quatro tipos de alimento, então é dito que ela é uma cidade real fortificada que não pode ser destruída por inimigos externos ou aliados falazes. </p>

 

<p class=Normal> “Do mesmo modo, bhikkhus, quando um nobre discípulo está dotado com sete qualidades verdadeiras,  (<i>saddhamma</i>), e pode obter de acordo com a sua vontade,  sem problemas e sem dificuldades, os quatro jhanas, estados mentais superiores que proporcionam uma estada prazerosa aqui e agora, é dito que ele é um nobre discípulo que não pode ser arruinado por Mara, não pode ser arruinado pelo Senhor do Mal. </p>

 

<p class=Normal> “Agora, quais são as sete qualidades verdadeiras com as quais ele está dotado? </p>

 

<p class=Normal> “Assim como uma cidade real fortificada tem pilastras como fundação – profundamente enraizadas, bem plantadas, inamovíveis e inabaláveis, para a proteção daqueles que estão dentro e para repelir aqueles que estão fora; do mesmo modo um nobre discípulo tem convicção comprovada no Buda assim: ‘O Abençoado é um arahant, perfeitamente iluminado, consumado no verdadeiro conhecimento e conduta, bem-aventurado, conhecedor dos mundos, um líder insuperável de pessoas preparadas para serem treinadas, mestre de devas e humanos, desperto, sublime.’ Com a convicção como a sua pilastra de fundação, o nobre discípulo abandona aquilo que é inábil, desenvolve aquilo que é hábil, abandona aquilo que é censurável, desenvolve aquilo que é isento de censura, e cuida de si mesmo de modo puro. Essa é a primeira qualidade verdadeira com a qual ele está dotado. [<a href="#N1">1</a>]</p>

 

<p class=Normal><a name="R1"></a>“Assim como uma cidade real fortificada tem um fosso, tanto profundo como largo, para a proteção daqueles que estão dentro e para repelir aqueles que estão fora; do mesmo modo um nobre discípulo tem vergonha de cometer transgressões. Ele sente vergonha do pensamento de praticar uma ação corporal imprópria, uma ação verbal imprópria, uma ação mental imprópria. Ele sente vergonha das ações ruins e prejudiciais. Com a vergonha como seu fosso, o nobre discípulo abandona aquilo que é inábil, desenvolve aquilo que é hábil, abandona aquilo que é censurável, desenvolve aquilo que é isento de censura, e cuida de si mesmo de modo puro. Essa é a segunda qualidade verdadeira com a qual ele está dotado. </p>

 

<p class=Normal> “Assim como uma cidade real fortificada tem uma plataforma que a circunda tanto alta como larga, para a proteção daqueles que estão dentro e para repelir aqueles que estão fora; do mesmo modo um nobre discípulo tem o temor de cometer transgressões. Ele sente temor do sofrimento que resulta por praticar uma ação corporal imprópria, uma ação verbal imprópria, uma ação mental imprópria. Ele sente temor das ações ruins e prejudiciais. Com o temor como a plataforma que o circunda, o nobre discípulo abandona aquilo que é inábil, desenvolve aquilo que é hábil, abandona aquilo que é censurável, desenvolve aquilo que é isento de censura, e cuida de si mesmo de modo puro. Essa é a terceira qualidade verdadeira com a qual ele está dotado. [<a href="#N2">2</a>]</p>

 

<p class=Normal><a name="R2"></a>  “Assim como uma cidade real fortificada tem muitas armas armazenadas, tanto flechas como coisas para serem arremessadas, para a proteção daqueles que estão dentro e para repelir aqueles que estão fora; do mesmo modo um nobre discípulo ouviu muito, reteve o que ouviu, memorizou o que ouviu. Todo ensinamento que é admirável no início, admirável no meio, admirável no final, com o correto significado e fraseado e que revela uma vida santa que é completamente perfeita e imaculada: esses ensinamentos ele ouviu com freqüência, memorizou, se recorda, discutiu, investigou com a sua mente e penetrou corretamente com o entendimento. Com o aprendizado como as suas armas, o nobre discípulo abandona aquilo que é inábil, desenvolve aquilo que é hábil, abandona aquilo que é censurável, desenvolve aquilo que é isento de censura, e cuida de si mesmo de modo puro. Essa é a quarta qualidade verdadeira com a qual ele está dotado. </p>

 

<p class=Normal> “Assim como uma cidade real fortificada tem um grande exército aquartelado no seu interior – elefantes, cavalaria, cocheiros, arqueiros, porta-estandartes, oficiais de alojamento, soldados da logística, príncipes conhecidos, heróis comandantes, infantaria e escravos - para a proteção daqueles que estão dentro e para repelir aqueles que estão fora; do mesmo modo um nobre discípulo mantém a sua energia estimulada para o abandono de qualidades mentais prejudiciais e para a obtenção de qualidades mentais benéficas; ele é decidido, firme no seu esforço, sem se esquivar das suas responsabilidades com relação ao cultivo de qualidades mentais benéficas. Com a energia como seu exército, o nobre discípulo abandona aquilo que é inábil, desenvolve aquilo que é hábil, abandona aquilo que é censurável, desenvolve aquilo que é isento de censura, e cuida de si mesmo de modo puro. Essa é a quinta qualidade verdadeira com a qual ele está dotado. </p>

 

<p class=Normal> “Assim como uma cidade real fortificada tem um guardião dos portões - sábio, competente e inteligente - que não permite a entrada de desconhecidos e admite que entrem os conhecidos, para a proteção daqueles que estão dentro e para repelir aqueles que estão fora; do mesmo modo um nobre discípulo tem atenção plena, é muito meticuloso, lembrando-se e capaz de chamar para a memória até mesmo as coisas que foram feitas e ditas há muito tempo. Com a atenção plena como seu guardião, o nobre discípulo abandona aquilo que é inábil, desenvolve aquilo que é hábil, abandona aquilo que é censurável, desenvolve aquilo que é isento de censura, e cuida de si mesmo de modo puro. Essa é a sexta qualidade verdadeira com a qual ele está dotado. [<a href="#N3">3</a>]</p>

 

<p class=Normal><a name="R3"></a>  “Assim como uma cidade real fortificada tem muralhas: altas e espessas e completamente revestidas com argamassa, para a proteção daqueles que estão dentro e para repelir aqueles que estão fora; do mesmo modo um nobre discípulo tem sabedoria, tem completa compreensão da origem e cessação – nobre, penetrante, que conduz ao fim do sofrimento. Com a sabedoria como o seu revestimento de argamassa, o nobre discípulo abandona aquilo que é inábil, desenvolve aquilo que é hábil, abandona aquilo que é censurável, desenvolve aquilo que é isento de censura, e cuida de si mesmo de modo puro. Essa é a sétima qualidade verdadeira com a qual ele está dotado. [<a href="#N4">4</a>]</p>

 

<p class=Normal><a name="R4"></a>  “Essas são as sete qualidades verdadeiras com as quais ele está dotado. </p>

 

<p class=Normal> “E quais são os quatro jhanas, estados mentais superiores que proporcionam uma estada prazerosa aqui e agora, que ele pode obter de acordo com a sua vontade,  sem problemas e sem dificuldades? </p>

 

<p class=Normal> “Assim como uma cidade real fortificada tem um grande estoque de capim, madeira e água para o prazer, comodidade e conforto daqueles que estão dentro e para repelir aqueles que estão fora; do mesmo modo um nobre discípulo, afastado dos prazeres sensuais, afastado das qualidades não hábeis, entra e permanece no primeiro jhana, que é caracterizado pelo pensamento aplicado e sustentado, com o êxtase e felicidade nascidos do afastamento - para o seu próprio prazer, comodidade e conforto, e para realizar nibbana. [<a href="#N5">5</a>]</p>

 

<p class=Normal><a name="R5"></a>  “Assim como uma cidade real fortificada tem grandes suprimentos de arroz e cevada para o prazer, comodidade e conforto  daqueles que estão dentro e para repelir aqueles que estão fora; do mesmo modo um nobre discípulo, abandonando o pensamento aplicado e sustentado, um bhikkhu entra e permanece no segundo jhana, que é caracterizado pela segurança interna e perfeita unicidade da mente, sem o pensamento aplicado e sustentado, com o êxtase e felicidade nascidos da concentração - para o seu próprio prazer, comodidade e conforto, e para realizar nibbana. </p>

 

<p class=Normal> “Assim como uma cidade real fortificada tem grandes suprimentos de gergelim, grão de bico e vários tipos de feijões para o prazer, comodidade e conforto  daqueles que estão dentro e para repelir aqueles que estão fora; do mesmo modo um nobre discípulo, abandonando o êxtase, um bhikkhu entra e permanece no terceiro jhana que é caracterizado pela felicidade sem o êxtase, acompanhada pela atenção plena, plena consciência e equanimidade, acerca do qual os nobres declaram: ‘Ele permanece numa estada feliz, equânime e plenamente atento’ - para o seu próprio prazer, comodidade e conforto, e para realizar nibbana. </p>

 

<p class=Normal> “Assim como uma cidade real fortificada tem grandes suprimentos de tônicos – manteiga líquida, manteiga fresca, azeite, mel, melaço e sal - para o prazer, comodidade e conforto  daqueles que estão dentro e para repelir aqueles que estão fora; do mesmo modo um nobre discípulo, que possui nem felicidade nem sofrimento, com a atenção plena e a equanimidade purificadas - para o seu próprio prazer, comodidade e conforto, e para realizar nibbana. </p>

 

<p class=Normal> “Esses são os quatro  jhanas, estados mentais superiores que proporcionam uma estada prazerosa aqui e agora, que ele pode obter de acordo com a sua vontade,  sem problemas e sem dificuldades. </p>

 

<p class=Normal> “Quando um nobre discípulo está dotado com sete qualidades verdadeiras,  e pode obter de acordo com a sua vontade,  sem problemas e sem dificuldades, os quatro jhanas, estados mentais superiores que proporcionam uma estada prazerosa aqui e agora, é dito que ele é um nobre discípulo que não pode ser arruinado por Mara, não pode ser arruinado pelo Senhor do Mal.” </p>

 

