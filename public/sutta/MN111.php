<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Em certa
ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no Parque de
Anathapindika. Lá ele se dirigiu aos monges desta forma: “Bhikkhus”. –
“Venerável Senhor,” eles responderam. O Abençoado disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><a
name=T2></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. “Bhikkhus,
Sariputta é sábio; Sariputta possui grande sabedoria; Sariputta possui ampla
sabedoria; Sariputta possui jubilosa sabedoria; Sariputta possui ágil
sabedoria; Sariputta possui aguçada sabedoria; Sariputta possui penetrante
sabedoria. Ao longo de meio mês, bhikkhus, Sariputta teve o insight dos estados
mentais, um a um, à medida que eles ocorreram. [</span><a href="#N1"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>1</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>] <a name=R1></a>Agora, o insight de Sariputta
dos estados mentais, um a um, à medida em que eles ocorreram foi o seguinte:<o:p></o:p></span></p>


<p class=Normal>
<span lang=PT-BR style='mso-ansi-language:PT-BR'>3. “Aqui, bhikkhus,
afastado dos prazeres sensuais, afastado das qualidades não hábeis, Sariputta
entrou e permaneceu no primeiro jhana, que é caracterizado pelo pensamento
aplicado e sustentado, com o êxtase e felicidade nascidos do afastamento.<o:p></o:p></span></p>


<p class=Normal>
<a name=T4></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. “E
os estados do primeiro jhana – pensamento aplicado, pensamento sustentado,
êxtase, felicidade e unificação da mente; contato, sensação, percepção, volição
e mente; zelo, decisão, energia, atenção plena <i>(sati)</i>, equanimidade e atenção
<i>(manasikara)</i> – esses estados foram definidos por ele, um a um, à medida que
eles ocorriam; [</span><a href="#N2"><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>] <a
name=R2></a>com o seu conhecimento esses estados surgiram, com o seu
conhecimento esses estados estiveram presentes, com o seu conhecimento esses
estados desapareceram. Ele compreendeu da seguinte forma: ‘De fato, esses
estados não tendo sido, vêm a ser; sendo, desaparecem.’ Em relação a esses
estados ele permaneceu sem se sentir atraído, sem sentir repulsa, desapegado,
livre, dissociado, com a mente livre de impedimentos. [</span><a href="#N3"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>3</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>] <a name=R3></a>Ele compreendeu que: ‘Há uma
escapatória mais adiante,’ e com o desenvolvimento daquela realização, ele
confirmou que havia. [</span><a href="#N4"><span lang=PT-BR style='mso-ansi-language:
PT-BR'>4</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>] <o:p></o:p></span></p>


<p class=Normal>
<span lang=PT-BR style='mso-ansi-language:PT-BR'>&nbsp;<a name=R4></a>5.
“Novamente, bhikkhus, abandonando o pensamento aplicado e sustentado, Sariputta
entrou e permaneceu no segundo jhana, que é caracterizado pela segurança interna e perfeita unicidade da mente, 
sem o pensamento aplicado e sustentado, com o êxtase e felicidade nascidos da concentração.<o:p></o:p></span></p>


<p class=Normal><span
lang=PT-BR style='mso-ansi-language:PT-BR'>6. “E os estados do segundo jhana –
êxtase, felicidade e unificação da mente; contato, sensação, percepção, volição
e mente; zelo, decisão, energia, atenção plena <i>(sati)</i>, equanimidade e atenção
<i>(manasikara)</i> – esses estados foram definidos por ele, um a um, à medida que
eles ocorriam; com o seu conhecimento esses estados surgiram, com o seu
conhecimento esses estados estiveram presentes, com o seu conhecimento esses
estados desapareceram. Ele compreendeu da seguinte forma… e com o
desenvolvimento daquela realização, ele confirmou que havia.<o:p></o:p></span></p>


<p class=Normal>
<span lang=PT-BR style='mso-ansi-language:PT-BR'>7. “Novamente, bhikkhus,
abandonando o êxtase, Sariputta entrou e permaneceu no terceiro jhana que é caracterizado pela felicidade 
sem o êxtase, acompanhada pela atenção plena, plena consciência e equanimidade, 
acerca do qual os nobres declaram: ‘Ele permanece numa estada feliz, equânime e plenamente atento.’<o:p></o:p></span></p>


<p class=Normal>
<span lang=PT-BR style='mso-ansi-language:PT-BR'>8. “E os estados do
terceiro jhana – a equanimidade, a felicidade, a atenção plena, a consciência
plena e a unificação da mente; contato, sensação, percepção, volição e mente;
zelo, decisão, energia, atenção plena, equanimidade e atenção – esses estados foram definidos por ele, um a um, à medida que eles
ocorriam; com o seu conhecimento esses estados surgiram, com o seu conhecimento
esses estados estiveram presentes, com o seu conhecimento esses estados
desapareceram. Ele compreendeu da seguinte forma… e com o desenvolvimento
daquela realização, ele confirmou que havia.<o:p></o:p></span></p>


<p class=Normal><span
lang=PT-BR style='mso-ansi-language:PT-BR'>9. “Novamente, bhikkhus, com o
completo desaparecimento da felicidade, Sariputta entrou e permaneceu no quarto jhana, que possui 
nem felicidade nem sofrimento, com a atenção plena e a equanimidade purificadas. <o:p></o:p></span></p>


<p class=Normal><span
lang=PT-BR style='mso-ansi-language:PT-BR'>10. “E os estados do quarto
jhana<span style="mso-spacerun: yes">  </span>- a equanimidade, a sensação nem
dolorosa, nem prazerosa, a despreocupação mental devido à tranqüilidade; [</span><a
href="#N5"><span lang=PT-BR style='mso-ansi-language:PT-BR'>5</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R5></a>a atenção plena
purificada e a unificação da mente; contato, sensação, percepção, volição e
mente; zelo, decisão, energia, atenção plena, equanimidade e
atenção – esses estados
foram definidos por ele, um a um, à medida que eles ocorriam; com o seu
conhecimento esses estados surgiram, com o seu conhecimento esses estados
estiveram presentes, com o seu conhecimento esses estados desapareceram. Ele compreendeu
da seguinte forma… e com o desenvolvimento daquela realização, ele confirmou
que havia.<o:p></o:p></span></p>


<p class=Normal><span
lang=PT-BR style='mso-ansi-language:PT-BR'>11. “Novamente, bhikkhus, com a
completa superação das percepções da forma, com o desaparecimento das
percepções do contato sensorial, sem dar atenção às percepções da diversidade,
consciente de que o ‘espaço é infinito,’ Sariputta entrou e permaneceu na base
do espaço infinito.<o:p></o:p></span></p>


<p class=Normal><span
lang=PT-BR style='mso-ansi-language:PT-BR'>12. “E os estados da base do espaço
infinito – a percepção da base do espaço infinito e a unificação da mente;
contato, sensação, percepção, volição e mente; zelo, decisão, energia, atenção
plena, equanimidade e atenção  – esses estados foram
definidos por ele, um a um, à medida que eles ocorriam; com o seu conhecimento
esses estados surgiram, com o seu conhecimento esses estados estiveram
presentes, com o seu conhecimento esses estados desapareceram. Ele compreendeu
da seguinte forma… e com o desenvolvimento daquela realização, ele confirmou
que havia.<o:p></o:p></span></p>


<p class=Normal><span
lang=PT-BR style='mso-ansi-language:PT-BR'>13. “Novamente, bhikkhus, com a
completa superação da base do espaço infinito, consciente de que a ‘consciência
é infinita,’ Sariputta entrou e permaneceu na base da consciência infinita.<o:p></o:p></span></p>


<p class=Normal><span
lang=PT-BR style='mso-ansi-language:PT-BR'>14. “E os estados da base da
consciência infinita – a percepção da base da consciência infinita e a
unificação da mente; contato, sensação, percepção, volição e mente; zelo,
decisão, energia, atenção plena, equanimidade e atenção –
esses estados foram definidos por ele, um a um, à medida que eles ocorriam; com
o seu conhecimento esses estados surgiram, com o seu conhecimento esses estados
estiveram presentes, com o seu conhecimento esses estados desapareceram. Ele
compreendeu da seguinte forma… e com o desenvolvimento daquela realização, ele
confirmou que havia.<o:p></o:p></span></p>


<p class=Normal><span
lang=PT-BR style='mso-ansi-language:PT-BR'>15. “Novamente, bhikkhus, com a
completa superação da base da consciência infinita, consciente de que ‘não há
nada,’ Sariputta entrou e permaneceu na base do nada. <o:p></o:p></span></p>


<p class=Normal><span
lang=PT-BR style='mso-ansi-language:PT-BR'>16. “E os estados da base do nada –
a percepção da base do nada e a unificação da mente; contato, sensação,
percepção, volição e mente; zelo, decisão, energia, atenção plena,
equanimidade e atenção – esses estados foram definidos por ele, um
a um, à medida que eles ocorriam; com o seu conhecimento esses estados
surgiram, com o seu conhecimento esses estados estiveram presentes, com o seu
conhecimento esses estados desapareceram. Ele compreendeu da seguinte forma… e
com o desenvolvimento daquela realização, ele confirmou que havia.<o:p></o:p></span></p>


<p class=Normal><span
lang=PT-BR style='mso-ansi-language:PT-BR'>17. “Novamente, bhikkhus, com a
completa superação da base do nada, Sariputta entrou e permaneceu na base da
nem percepção, nem não percepção.<o:p></o:p></span></p>


<p class=Normal><span
lang=PT-BR style='mso-ansi-language:PT-BR'>18. “Ele emergiu daquela realização
com atenção plena. Tendo feito isso, ele contemplou os estados passados que
haviam cessado e mudado, desta forma: ‘Então, realmente, esses estados não
tendo sido, vêm a ser; sendo, desaparecem.’ [</span><a href="#N6"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>6</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>] <a name=R6></a>Em relação a esses estados ele
permaneceu sem se sentir atraído, sem sentir repulsa, desapegado, livre,
dissociado, com a mente livre de impedimentos. Ele compreendeu que: ‘Há uma
escapatória mais adiante,’ e com o desenvolvimento daquela realização, ele
confirmou que havia.<o:p></o:p></span></p>


<p class=Normal><span
lang=PT-BR style='mso-ansi-language:PT-BR'>19. “Novamente, bhikkhus, com a
completa superação da base da nem percepção, nem não percepção, Sariputta
entrou e permaneceu na cessação da percepção e sensação. E as suas impurezas
foram destruidas ao ver com sabedoria. [</span><a href="#N7"><span lang=PT-BR
style='mso-ansi-language:PT-BR'>7</span></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>]<o:p></o:p></span></p>


<p class=Normal><a
name=R7></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>20. “Ele emergiu
daquela realização com atenção plena. Tendo feito isso, ele contemplou os
estados passados que haviam cessado e mudado, desta forma: ‘Então, realmente,
esses estados não tendo sido, vêm a ser; sendo, desaparecem.’ [</span><a
href="#N8"><span lang=PT-BR style='mso-ansi-language:PT-BR'>8</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R8></a>Em relação a esses
estados ele permaneceu sem se sentir atraído, sem sentir repulsa, desapegado,
livre, dissociado, com a mente livre de impedimentos. Ele compreendeu que:<span
style="mso-spacerun: yes">  </span>‘Não há uma escapatória mais adiante,’ e com
o desenvolvimento daquela realização, ele confirmou que não havia. [</span><a
href="#N9"><span lang=PT-BR style='mso-ansi-language:PT-BR'>9</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>


<p class=Normal><a
name=R9></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>21. “Bhikkhus,
falando corretamente, se fosse para dizer de alguém que: ‘Ele obteve maestria e
perfeição [</span><a href="#N10"><span lang=PT-BR style='mso-ansi-language:
PT-BR'>10</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>] <a
name=R10></a>na nobre virtude, obteve maestria e perfeição na nobre
concentração, obteve maestria e perfeição na nobre sabedoria, obteve maestria e
perfeição na nobre libertação,’ é de Sariputta que, de fato,<span
style="mso-spacerun: yes">  </span>falando corretamente, isso deveria ser dito.<o:p></o:p></span></p>


<p class=Normal><span
lang=PT-BR style='mso-ansi-language:PT-BR'>22. “Bhikkhus, falando corretamente,
se fosse para dizer de alguém que: ‘Ele é o filho do Abençoado, nascido do seu
peito, nascido da sua boca, nascido do Dhamma, criado pelo Dhamma, um herdeiro
do Dhamma, não um herdeiro de coisas materiais,’ é de Sariputta que, de fato,
falando corretamente,<span style="mso-spacerun: yes">  </span>isso deveria ser
dito.<o:p></o:p></span></p>


<p class=Normal><span
lang=PT-BR style='mso-ansi-language:PT-BR'>23. “Bhikkhus, a insuperável Roda do
Dhamma colocada em movimento pelo Tathagata é mantida em movimento corretamente
por Sariputta.”<o:p></o:p></span></p>


<p class=Normal><span
lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o que disse o Abençoado. Os
bhikkhus<span style="mso-spacerun: yes">  </span>ficaram satisfeitos e
contentes com as palavras do Abençoado.<o:p></o:p></span></p>


