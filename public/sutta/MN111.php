<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] <i>Anupadadhammavipassana</i>. MA explica que ele desenvolveu o
insight dos fatores de jhana em ordem sucessiva através das realizações meditativas, conforme será descrito. O período de duas semanas
mencionado ocorreu do momento da ordenação do Ven. Sariputta até a sua
realização do estado de arahant, ao ouvir a explicação do Buda para Dighanakha
sobre a compreensão das sensações (<a href="MN74.php#T14">MN 74.14</a>). [<a
href="#R1">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[2] Os primeiros cinco fatores são os próprios fatores do primeiro
jhana; os fatores seguintes são componentes adicionais, cada um desempenhando a
sua função individual como parte do primeiro jhana. Essa análise minuciosa dos
fatores mentais antecipa a metodologia do Abhidhamma, e assim não é por mera
coincidência que o nome de Sariputta esteja tão intimamente ligado com a
literatura do Abhidhamma. [<a href="#R2">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[3] Todos esses termos significam a supressão temporária dos
obstáculos através do poder de jhana, não se trata da libertação completa
das contaminações através da sua erradicação por meio do caminho supramundano, (arahant),
que o Ven. Sariputta ainda não havia alcançado. [<a href="#R3">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[4] A “escapatória mais adiante” (<i>uttarim nissaranam</i>) neste caso
é a próxima realização, o segundo jhana. [<a href="#R4">Retorna</a>]<o:p></o:p></span></p>

