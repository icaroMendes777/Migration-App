<p class=Normal> Em Savatthi. Então um certo bhikkhu foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse: </p>

<p class=Normal> “Venerável senhor, seria bom se o Abençoado pudesse me ensinar o Dhamma de forma resumida, de modo que tendo ouvido o Dhamma do Abençoado eu possa permanecer só, isolado, diligente, ardente e decidido.” </p>

<p class=Normal> “Bhikkhu, você deve abandonar o desejo por tudo que é sedutor.” </p>

<p class=Normal> “Eu compreendo, Abençoado! Eu Compreendo, Iluminado!” </p>

<p class=Normal> “E como, bhikkhu, você compreende o significado em detalhe do que eu disse de forma resumida?” </p>

<p class=Normal> “A forma, venerável senhor, é sedutora; eu devo abandonar o desejo por ela. A sensação é sedutora ... a percepção é sedutora ... as formações volitivas são sedutoras ... a consciência é sedutora; eu devo abandonar o desejo por ela. </p>

<p class=Normal> “Senhor, assim é como compreendo o significado em detalhe do que o Abençoado disse de forma resumida.” </p>

<p class=Normal> “Muito bem, bhikkhu. É bom que seja assim que você tenha entendido o significado em detalhe do que eu disse de forma resumida. A forma é sedutora ... a consciência é sedutora; você deve abandonar o desejo por ela. Assim é como o significado em detalhe do que eu disse de forma resumida deve ser entendido.” </p>

<p class=Normal> Então o bhikkhu, satisfeito e contente com as palavras do Abençoado, levantou do seu assento e depois de homenagear o Abençoado, mantendo-o à sua direita, partiu. Depois, permanecendo só, isolado, diligente, ardente e decidido, em pouco tempo, ele alcançou e permaneceu no objetivo supremo da vida santa pelo qual membros de um clã deixam a vida em família pela vida santa, tendo conhecido e realizado por si mesmo no aqui e agora. Ele soube: “O nascimento foi destruído, a vida santa foi vivida, o que deveria ser feito foi feito, não há mais vir a ser a nenhum estado.” E assim ele se tornou mais um dos arahants. </p>


