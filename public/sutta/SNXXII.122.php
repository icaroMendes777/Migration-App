<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Certa
ocasião, o ven. Sariputta e o ven. Maha Kotthita estavam em Benares
no Parque do Gamo, em Isipatana. Então, o ven. Maha Kotthita, saindo do seu
isolamento no final da tarde foi até o ven. Sariputta e ao
chegar ambos se cumprimentaram. Quando a conversa cortês e amigável havia
terminado ele sentou a um lado e disse: “Sariputta, meu amigo, quais coisas um
bhikkhu virtuoso deveria observar com atenção com sabedoria?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Um bhikkhu
virtuoso, meu amigo Kotthita, deveria observar com atenção com sabedoria os cinco agregados
influenciados pelo apego como impermanentes, sofrimento, uma enfermidade, um
câncer, uma flecha, dolorosos, uma aflição, estranhos, uma dissolução, um
vazio, não-eu. Quais cinco? A forma como um agregado influenciado pelo apego,
a sensação ... a percepção ... as formações volitivas ... a consciência como um agregado
influenciado pelo apego. Um bhikkhu virtuoso deveria observar com atenção com sabedoria esses
cinco agregados influenciados pelo apego como impermanentes, sofrimento, uma
enfermidade, um câncer, uma flecha, dolorosos, uma aflição, estranhos, uma
dissolução, um vazio, não-eu. Pois, é possível que, um bhikkhu virtuoso, que
observe com atenção com sabedoria esses cinco agregados como impermanentes ... não-eu, possa
alcançar o fruto de ‘entrar na correnteza’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
quais coisas deveria um bhikkhu, que ‘entrou na correnteza’, observar com
atenção com sabedoria?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Um
bhikkhu, que ‘entrou na correnteza,’ deveria observar com atenção com sabedoria os cinco
agregados influenciados pelo apego como impermanentes, sofrimento, uma
enfermidade, um câncer, uma flecha, dolorosos, uma aflição, estranhos, uma
dissolução, um vazio, não-eu. Pois, é possível que um bhikkhu, que ‘entrou na
correnteza,’ que observe com atenção com sabedoria esses cinco agregados como impermanentes
