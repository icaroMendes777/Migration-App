<p class=Normal> Em certa ocasião o Abençoado estava em Vesali no salão com um pico na cumeeira na Grande Floresta, junto com um número de conhecidos bhikkhus seniores: o venerável Cala, o venerável Upacala, o venerável Kakkata, o venerável Katimbha, o venerável Kata, o venerável Katissanga, e outros bem conhecidos discípulos seniores. </p>

<p class=Normal>Agora naquela ocasião um número de conhecidos Licchavis haviam entrado na Grande Floresta para ver o Abençoado, e ao seguirem um ao outro nas suas elegantes carruagens eles faziam muito ruído e algazarra. Então aqueles veneráveis pensaram: "Um número de conhecidos Licchavis entraram na Grande Floresta para ver o Abençoado, e ao seguirem um ao outro nas suas elegantes carruagens eles estão fazendo muito ruído e algazarra. Agora o Abençoado disse que o ruído é um espinho para os jhanas. Vamos para o bosque das árvores sal de Gosinga. Lá poderemos ficar tranquilos, sem ruído e sem qualquer multidão." Então aqueles veneráveis foram para o bosque das árvores sal de Gosinga e lá permaneceram tranquilos, sem ruído e sem qualquer multidão.

<p class=Normal>Então o Abençoado se dirigiu aos bhikkhus: Bhikkhus, onde está Cala? Onde está Upacala? Onde está Kakkata? Onde está Katimbha? Onde está Kata? Onde está Katissanga? Para onde foram esses discípulos seniores?" </p>

<p class=Normal>"Venerável senhor, esses veneráveis pensaram: 'Um número de conhecidos Licchavis entraram na Grande Floresta para ver o Abençoado, e ao seguirem um ao outro nas suas elegantes carruagens eles estão fazendo muito ruído e algazarra. Agora o Abençoado disse que o ruído é um espinho para os jhanas. Vamos para o bosque das árvores sal de Gosinga. Lá poderemos ficar tranquilos, sem ruído e sem qualquer multidão.' Assim eles foram para o bosque das árvores sal de Gosinga onde permanecem tranquilos, sem ruído e sem qualquer multidão." </p>

<p class=Normal>"Muito bem, bhikkhus! Esses discípulos falaram corretamente quando disseram ter eu dito ser o ruído um espinho para os jhanas. Bhikkhus, há esses dez espinhos. Quais dez? (1) Deleite com as companhias é um espinho para quem se delicia com o isolamento, (2) a busca de um objeto atraente é um espinho para quem intenciona pela meditação no sinal do repulsivo, (3) um espetáculo  é um espinho para quem guarda as portas dos meios dos sentidos, (4) manter companhia com mulheres é um espinho para a vida celibatária, (5) ruído é um espinho para o primeiro jhana. [<a href="#N1">1</a>] (6) pensamento aplicado e pensamento sustentado são um espinho para o segundo jhana, (7) êxtase é um espinho para o terceiro jhana, (8) inspiração e expiração é um espinho para o quarto jhana, (9) percepção e sensação são um espinho para a cessação da percepção e sensação, (10) cobiça é um espinho, raiva é um espinho, delusão é um espinho. Permaneçam sem espinhos, bhikkhus! Permaneçam sem espinhos, bhikkhus! Os arahants não têm espinhos. Os arahants estão desprovidos de espinhos e não têm espinhos."
</p>


