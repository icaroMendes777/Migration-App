<p class=Normal> Assim ouvi. Em certa ocasião o Abençoado estava perambulando por Kosala com uma grande sangha de bhikkhus até que por fim acabou chegando no vilarejo brâmane denominado Icchanangala, ficando na floresta de Icchanangala. </p>

<p class=Normal>Os brâmanes chefes de família de Icchanangala ouviram: “Gotama o contemplativo, o filho dos Sakyas, que adotou a vida santa deixando o clã dos Sakyas, que andava perambulando em Kosala com um grande número de bhikkhus chegou em Icchanangala. E acerca desse mestre Gotama existe essa boa reputação: ‘Esse Abençoado é um arahant, perfeitamente iluminado, consumado no verdadeiro conhecimento e conduta, bem-aventurado, conhecedor dos mundos, um líder insuperável de pessoas preparadas para serem treinadas, mestre de devas e humanos, desperto, sublime. Ele declara - tendo realizado por si próprio com o conhecimento direto - este mundo com os seus devas, maras e brahmas, esta população com seus contemplativos e brâmanes, seus príncipes e o povo. Ele ensina o Dhamma com o significado e fraseado corretos, que é admirável no início, admirável no meio, admirável no final; e ele revela uma vida santa que é completamente perfeita e imaculada.’ É bom poder encontrar alguém tão nobre.” </p>

 <p class=Normal>Então os brâmanes chefes de família de Icchanangala, quando a noite havia terminado, tomando muitos alimentos de vários tipos foram até a guarita no portão para a floresta de Icchanangala. Ao chegarem eles ficaram ali parados fazendo uma ruidosa algazarra, uma grande algazarra. </p>

 <p class=Normal>Agora naquela ocasião, o Venerável Nagita era o acompanhante do Abençoado. Então o Abençoado disse para o Venerável Nagita: “Nagita, que ruidosa algazarra é essa, essa grande algazarra, igual a pescadores vendendo peixes?” </p>

 <p class=Normal> “Venerável senhor, esses são os brâmanes chefes de família de Icchanangala parados na guarita do portão para a floresta de Icchanangala e que trouxeram muitos alimentos de vários tipos para o Abençoado e a Sangha dos bhikkhus.” </p>

 <p class=Normal> “Que eu não tenha nada que ver com honrarias, Nagita, e que as honrarias não tenham nada que ver comigo. Qualquer um que não possa obter de acordo com a sua vontade, sem dificuldades e sem problemas, como eu obtenho, o prazer da renúncia, o prazer do isolamento, o prazer da paz, o prazer da iluminação, que ele aceite esse prazer excrementício pegajoso, esse prazer torporoso, esse prazer dos ganhos, honrarias e fama.” </p>

 <p class=Normal> “Venerável senhor, que o Abençoado aceite [as oferendas deles] agora! Que o iluminado aceite agora! Agora é o momento para que o Abençoado aceite! Agora é o momento para que o Abençoado aceite! Onde quer que o Abençoado vá, os brâmanes das cidades e do campo assim estarão propensos. Tal como quando os devas da chuva mandam gotas pesadas de chuva e as águas fluem de acordo com a inclinação, da mesma forma, onde quer que o Abençoado vá, os brâmanes das cidades e do campo assim estarão propensos. Por que isso? Porque essa é a virtude e a sabedoria do Abençoado.” </p>

 <p class=Normal> “Que eu não tenha nada que ver com honrarias, Nagita, e que as honrarias não tenham nada que ver comigo. Qualquer um que não possa obter de acordo com a sua vontade, sem dificuldades e sem problemas, como eu obtenho, o prazer da renúncia, o prazer do isolamento, o prazer da paz, o prazer da iluminação, que ele aceite esse prazer excrementício pegajoso, esse prazer torporoso, esse prazer dos ganhos, honrarias e fama. </p>

 <p class=Normal> “Quando alguém come, bebe, mastiga e saboreia, há o excremento e urina: essa é a recompensa dele.

 
<p class=Normal> “Quando alguém ama, surge então a mudança e alteração, tristeza, lamentação, dor, angústia e desespero: essa é a recompensa dele.

 <p class=Normal> “Quando alguém está comprometido com o tema da repulsa, ele se decide pelo sinal da repulsa naquilo que é belo: essa é a recompensa dele.

 <p class=Normal> “Quando alguém permanece focado na impermanência do contato nos seis meios dos sentidos, ele se decide pela repulsa ao contato: essa é a recompensa dele.

 <p class=Normal> “Quando alguém permanece focado na origem e cessação dos cinco agregados influenciados pelo apego, ele se decide pela repulsa ao apego: essa é a recompensa dele."
