<p class=Normal>Assim ouvi. Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no Parque de Anathapindika. Então, quando a noite estava bem avançada, um número de devatas com belíssima aparência que iluminaram todo o Bosque de Jeta, se aproximaram do Abençoado. Ao se aproximar elas homenagearam o Abençoado e ficaram em pé a um lado. Uma devata então disse: </p>

<p class=Normal style='margin-left:.75in'>"Devido à avareza e negligência<br>
uma oferenda não é dada. <br>
Quem sabe, desejando méritos, <br>
com certeza deve dar oferendas." </p>

<p class=Normal>Então uma outra devata disse o seguinte: </p>

<p class=Normal style='margin-left:.75in'>"Aquilo que o ávaro teme quando ele não dá<br>
é exatamente o perigo que ameaça o não doador. <br>
A fome e a sede que o ávaro teme<br>
afligem esse tolo neste mundo e no próximo. </p>

<p class=Normal style='margin-left:.75in'>Portanto, tendo removido a avareza, <br>
quem eliminou essa mácula deve dar oferendas. <br>
Méritos é aquilo que sustenta os seres vivos<br>
ao surgirem no outro mundo." </p>

<p class=Normal>Então uma outra devata disse o seguinte: </p>

<p class=Normal style='margin-left:.75in'>"Eles não morrem entre aqueles que estão mortos<br>
quem, como companheiros viajantes na estrada, <br>
oferecem embora tenham pouco: <br>
esse é um princípio antigo. [<a href="#N1">1</a>] </p>

<p class=Normal style='margin-left:.75in'><a name=R1></a>Alguns dão do pouco que têm, <br>
outros que são ricos não gostam de dar. <br>
Uma oferenda dada do pouco que se tenha<br>
vale mil vezes o seu valor." </p>

<p class=Normal>Então uma outra devata disse o seguinte: </p>

<p class=Normal style='margin-left:.75in'>"Os maus não emulam os bons, <br>
que dão aquilo que é difícil de dar, <br>
e fazem o que é difícil ser feito: <br>
o Dhamma dos bondosos é difícil de ser seguido. </p>

<p class=Normal style='margin-left:.75in'>Assim o destino após a morte<br>
do bom difere do mau: <br>
o mau vai para o inferno, <br>
o bom destinado ao paraíso." </p>

<p class=Normal>Então uma outra devata disse para o Abençoado: "Quem falou bem, Abençoado?" </p>

<p class=Normal>"Todas falaram bem no seu modo. Mas ouçam o que tenho a dizer: </p>

<p class=Normal style='margin-left:.75in'>Se alguém pratica o Dhamma<br>
embora sobreviva apenas daquilo que colhe, <br>
enquanto mantém a esposa<br>
ele dá do pouco que tem, <br>
então nem cem mil oferendas<br>
daqueles que sacrificam mil<br>
não valem nem uma fração<br>
de uma oferenda dada por alguém como ele. </p>

<p class=Normal>Então uma outra devata disse o seguinte: </p>

<p class=Normal style='margin-left:.75in'>"Por que a oferenda deles, vasta e imensa, <br>
não tem o mesmo valor da oferenda da pessoa íntegra? <br>
Por que cem mil oferendas<br>
daqueles que sacrificam mil<br>
não valem nem uma fração<br>
de uma oferenda dada por alguém como ele?" </p>

<p class=Normal>Então o Abençoado respondeu: </p>

<p class=Normal style='margin-left:.75in'>"Visto que eles dão estabelecidos naquilo que é improbo, <br>
tendo morto e assassinado, causando tristeza, <br>
a sua oferenda - lacrimosa, carregada de violência -<br>
não compartilha o valor dado por quem é probo. <br>
Por isso que cem mil oferendas<br>
daqueles que sacrificam mil<br>
não valem nem uma fração<br>
de uma oferenda dada por alguém como ele." </p>


