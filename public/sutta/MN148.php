<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(AS
TENDÊNCIAS SUBJACENTES)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>28. (i)
“Bhikkhus, na dependência do olho e das formas, [</span><a href="#N8"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>8</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>] <a name=R8></a>a consciência no olho surge; o
encontro dos três é o contato; com o contato como condição surge uma sensação
sentida como prazerosa ou dolorosa ou nem prazerosa, nem dolorosa. Quando
alguém é tocado por uma sensação prazerosa, se ele sente prazer com ela,
recebe-a e permanece agarrado a ela, então a tendência subjacente ao desejo
está nele. Quando alguém é tocado por uma sensação dolorosa, se ele fica
triste, angustiado e lamenta, chora, bate no peito e fica perturbado, então a
tendência subjacente à aversão está nele. Quando alguém é tocado por uma
sensação nem prazerosa, nem dolorosa, se ele não compreende como na verdade é a
origem, a cessação, a gratificação, o perigo e a escapatória em relação àquela
sensação, então a tendência subjacente à ignorância está nele. Bhikkhus, que
alguém possa aqui e agora dar um fim ao sofrimento sem abandonar a tendência
subjacente do desejo em relação às sensações prazerosas, sem abolir a tendência
subjacente da aversão em relação às sensações dolorosas, sem extirpar a
tendência subjacente da ignorância em relação às sensações nem prazerosas, nem
dolorosas; sem abandonar a ignorância e fazer surgir o verdadeiro conhecimento
[</span><a href="#N9"><span lang=PT-BR style='mso-ansi-language:PT-BR'>9</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] –<a name=R9></a> isso é
impossível.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>29-33.
(ii-vi) “Bhikkhus, na dependência do ouvido e dos sons, a consciência no ouvido
surge ... Na dependência da mente e dos objetos mentais, a consciência na mente
surge; o encontro dos três é o contato; com o contato como condição surge uma
sensação sentida como prazerosa ou dolorosa ou nem prazerosa, nem dolorosa ...
Bhikkhus, que alguém possa aqui e agora dar um fim ao sofrimento sem abandonar
a tendência subjacente do desejo em relação às sensações prazerosas, sem abolir
a tendência subjacente da aversão em relação às sensações dolorosas, sem
extirpar a tendência subjacente da ignorância em relação às sensações nem
prazerosas, nem dolorosas; sem abandonar a ignorância e fazer surgir o
verdadeiro conhecimento – isso é impossível.. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(O ABANDONO
DAS<span style="mso-spacerun: yes">  </span>TENDÊNCIAS SUBJACENTES)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>34. (i)
“Bhikkhus, na dependência do olho e das formas, a consciência no olho surge; o
encontro dos três é o contato; com o contato como condição surge uma sensação
sentida como prazerosa ou dolorosa ou nem prazerosa, nem dolorosa. Quando
alguém é tocado por uma sensação prazerosa, se ele não sente prazer com ela, se
não a recebe e não permanece agarrado a ela, então a tendência subjacente ao
desejo não está nele. Quando alguém é tocado por uma sensação dolorosa, se ele
não fica triste, angustiado e lamenta, não chora, bate no peito e não fica
perturbado, então a tendência subjacente à aversão não está nele. Quando alguém
é tocado por uma sensação nem prazerosa, nem dolorosa, se ele compreende como
na verdade é a origem, a cessação, a gratificação, o perigo e a escapatória em
relação àquela sensação, então a tendência subjacente à ignorância não está
nele. Bhikkhus, que alguém possa aqui e agora dar um fim ao sofrimento através
do abandono da tendência subjacente do desejo em relação às sensações
prazerosas, abolindo a tendência subjacente da aversão em relação às sensações
dolorosas, extirpando a tendência subjacente da ignorância em relação às
sensações nem prazerosas, nem dolorosas; abandonando a ignorância e fazendo
surgir o verdadeiro conhecimento – isso é possível.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>35-39.
(ii-vi) “Bhikkhus, na dependência do ouvido e dos sons, a consciência no ouvido
surge ... Na dependência da mente e dos objetos mentais, a consciência na mente
surge; o encontro dos três é o contato; com o contato como condição surge uma
sensação sentida como prazerosa ou dolorosa ou nem prazerosa, nem dolorosa ...
Bhikkhus, que alguém possa aqui e agora dar um fim ao sofrimento através do
abandono da tendência subjacente do desejo em relação às sensações prazerosas,
abolindo a tendência subjacente da aversão em relação às sensações dolorosas,
extirpando a tendência subjacente da ignorância em relação às sensações nem
prazerosas, nem dolorosas; abandonando a ignorância e fazendo surgir o
verdadeiro conhecimento – isso é possível.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(LIBERTAÇÃO)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>40. “Vendo
dessa<span style="mso-spacerun: yes">  </span>forma, bhikkhus, um nobre
discípulo bem instruído se desencanta do olho, se desencanta das formas, se
desencanta da consciência no olho, se desencanta do contato no olho, se
desencanta da sensação, se desencanta do desejo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele se
desencanta do ouvido ... Ele se desencanta do nariz ... se desencanta da língua …
se desencanta do corpo ... se desencanta da mente, se desencanta dos objetos
mentais, se desencanta da consciência na mente, se desencanta do contato na
mente, se desencanta da sensação, se desencanta do desejo. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>41.
‘Desencantado, ele se torna desapegado. Através do desapego a sua mente é
libertada. Quando ela está libertada surge o conhecimento: </span>‘Libertada.’
Ele compreende que: ‘O nascimento foi destruído, a vida santa foi vivida, o que
deveria ser feito foi feito, não há mais vir a ser a nenhum estado.’<span
lang=PT-BR style='mso-ansi-language:PT-BR'>”<o:p></o:p></span></p>



