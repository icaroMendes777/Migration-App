nariz surge ... com a sensação como condição surge o desejo. Na dependência da
língua e dos sabores, a consciência na língua surge ... com a sensação como
condição surge o desejo. Na dependência do corpo e dos tangíveis, a consciência
no corpo surge ... com a sensação como condição surge o desejo. Na dependência
da mente e dos objetos mentais, a consciência na mente surge; o encontro dos
três é o contato; com o contato como condição surge a sensação; com a sensação
como condição surge o desejo. Portanto, foi com referência a isso que foi dito:
’As seis classes de desejo devem ser compreendidas.’ Este é o sexto conjunto de
seis.<o:p></o:p></span></p>



