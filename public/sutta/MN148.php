<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no
Parque de Anathapindika. Lá ele se dirigiu aos monges desta forma: “Bhikkhus”. –
“Venerável Senhor,” eles responderam. O Abençoado disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.
“Bhikkhus, eu ensinarei para vocês o Dhamma </span>que é admirável no início,
admirável no meio, admirável no final, com o correto significado e fraseado<span
lang=PT-BR style='mso-ansi-language:PT-BR'>; eu revelarei a vida santa que é
completamente perfeita e imaculada: [</span><a href="#N1"><span lang=PT-BR
style='mso-ansi-language:PT-BR'>1</span></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>] <a name=R1></a>isto é, os seis conjuntos de seis. Ouçam e prestem
muita atenção ao que vou dizer.” – “Sim, venerável senhor,” os bhikkhus
responderam. O Abençoado disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(SINOPSE)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. “As seis
bases internas devem ser compreendidas. As seis bases externas devem ser
compreendidas.<span style="mso-spacerun: yes">  </span>As seis classes de
consciência devem ser compreendidas. As seis classes de contato devem ser
compreendidas. As seis classes de sensações devem ser compreendidas. As seis
classes de desejo devem ser compreendidas.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(ENUMERAÇÃO)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. (i) “’As
seis bases internas devem ser compreendidas.’ Assim foi dito. E com referência
a que foi dito isso? Existe a base do olho, a base do ouvido, a base do nariz,
a base da língua, a base do corpo e a base da mente. Portanto, foi com
referência a isso que foi dito:<span style="mso-spacerun: yes">  </span>‘As
seis bases internas devem ser compreendidas.’ Este é o primeiro conjunto de
seis.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. (ii)
“’As seis bases externas devem ser compreendidas.’ Assim foi dito. E com
referência a que foi dito isso? Existe a base da forma, a base do som, a base
do aroma, a base do sabor, a base dos tangíveis e a base do objeto mental.
Portanto, foi com referência a isso que foi dito:<span style="mso-spacerun:
yes">  </span>’As seis bases externas devem ser compreendidas.’ Este é o
segundo conjunto de seis. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. (iii)
“’As seis classes de consciência devem ser compreendidas.’ Assim foi dito. E
com referência a que foi dito isso? Na dependência do olho e das formas, a
consciência no olho surge; na dependência do ouvido e dos sons, a consciência
no ouvido surge; na dependência do nariz e dos aromas, a consciência no nariz
surge; na dependência da língua e dos sabores, a consciência na língua surge;
na dependência do corpo e dos tangíveis, a consciência no corpo surge; na
dependência da mente e dos objetos mentais, a consciência na mente surge.
Portanto, foi com referência a isso que foi dito:<span style="mso-spacerun:
yes">  </span>’As seis classes de consciência devem ser compreendidas.’ Este é
o terceiro conjunto de seis.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. (iv)
“’As seis classes de contato devem ser compreendidas.’ Assim foi dito. E com
referência a que foi dito isso?<span style="mso-spacerun: yes">  </span>Na
dependência do olho e das formas, a consciência no olho surge; o encontro dos
três é o contato. Na dependência do ouvido e dos sons, a consciência no ouvido
surge; o encontro dos três é o contato. Na dependência do nariz e dos aromas, a
consciência no nariz surge; o encontro dos três é o contato. Na dependência da
língua e dos sabores, a consciência na língua surge; o encontro dos três é o
contato. Na dependência do corpo e dos tangíveis, a consciência no corpo surge;
o encontro dos três é o contato. Na dependência da mente e dos objetos mentais,
a consciência na mente surge; o encontro dos três é o contato. Portanto, foi
com referência a isso que foi dito: ’As seis classes de contato devem ser
compreendidas.’ Este é o quarto conjunto de seis.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. (v) “’As
seis classes de sensações devem ser compreendidas.’ Assim foi dito. E com
referência a que foi dito isso?<span style="mso-spacerun: yes">  </span>Na
dependência do olho e das formas, a consciência no olho surge; o encontro dos
três é o contato; com o contato como condição surge a sensação. Na dependência
do ouvido e dos sons, a consciência no ouvido surge; o encontro dos três é o
contato; com o contato como condição surge a sensação. Na dependência do nariz
e dos aromas, a consciência no nariz surge; o encontro dos três é o contato;
com o contato como condição surge a sensação. Na dependência da língua e dos
sabores, a consciência na língua surge; o encontro dos três é o contato; com o
contato como condição surge a sensação. Na dependência do corpo e dos
tangíveis, a consciência no corpo surge; o encontro dos três é o contato; com o
contato como condição surge a sensação. Na dependência da mente e dos objetos
mentais, a consciência na mente surge; o encontro dos três é o contato; com o
contato como condição surge a sensação. Portanto, foi com referência a isso que
foi dito: ‘As seis classes de sensações devem ser compreendidas.’ Este é o
quinto conjunto de seis.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. (vi)
“’As seis classes de desejo devem ser compreendidas.’ Assim foi dito. E com
referência a que foi dito isso? Na dependência do olho e das formas, a
consciência no olho surge; o encontro dos três é o contato; com o contato como
condição surge a sensação; com a sensação como condição surge o desejo.[</span><a
