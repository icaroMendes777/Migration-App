<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. Então, o Venerável Bahiya foi até o Abençoado e depois de
cumprimentá-lo sentou a um lado e disse:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, seria bom se o Abençoado pudesse me ensinar o Dhamma de maneira
resumida, de modo que, tendo ouvido o Dhamma do Abençoado, eu possa permanecer
só, isolado, diligente, ardente e decidido.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bem então,
Bahiya, purifique o ponto exato de início dos estados benéficos. [<a href="#N1">1</a>]
<a name=R1></a>E qual é o início dos estados benéficos? A virtude que é bem
purificada e o entendimento que é correto. Então, Bahiya, quando a sua virtude
estiver bem purificada e o seu entendimento correto, baseado na virtude,
estabelecido na virtude, você deve desenvolver os quatro fundamentos da atenção
plena de três modos.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quais
quatro? Neste caso, bhikkhu, permaneça contemplando o corpo como um corpo,
ardente, plenamente consciente e com atenção plena, tendo colocado de lado a
cobiça e o desprazer pelo mundo. Permaneça contemplando as sensações como
sensações ... a mente como mente ... os objetos mentais como objetos mentais,
ardente, plenamente consciente e com atenção plena, tendo colocado de lado a
cobiça e o desprazer pelo mundo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando,
Bahiya, baseado na virtude, estabelecido na virtude, você desenvolver os quatro
fundamentos da atenção plena dessa forma, então, quer venha a noite ou o dia,
você poderá esperar apenas o incremento nos estados benéficos, não o seu declínio.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o
Venerável Bahiya, tendo ficado satisfeito e contente com as palavras do
Abençoado, levantou-se do seu assento e depois de homenageá-lo, mantendo-o à
sua direita, partiu. Permanecendo só, isolado, diligente, ardente e decidido,
em pouco tempo, ele alcançou e permaneceu no objetivo supremo da vida santa
pelo qual membros de um clã deixam a vida em família pela vida santa, tendo
conhecido e realizado por si mesmo no aqui e agora. Ele soube: “O nascimento
foi destruído, a vida santa foi vivida, o que deveria ser feito foi feito, não
há mais vir a ser a nenhum estado.” E assim o Venerável Bahiya tornou-se mais
um dos Arahants.<o:p></o:p></span></p>




