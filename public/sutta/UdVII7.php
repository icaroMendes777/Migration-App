<p class=Normal> Assim, ouvi. Certa ocasião, o Abençoado estava em Savatthi, no Bosque de Jeta, no Parque de Anathapindika. Agora, naquela ocasião, o Abençoado estava revisando o seu próprio abandono das percepções e conceitos nascidos da proliferação. [<a href="#N1">1</a>]</p>

<p class=Normal>Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou:</p>

<p class=Normal style='margin-left:.75in'> O mundo com os seus devas não menospreza
o sábio liberto do desejo,
em quem não há proliferações nem continuidade,
tendo vencido a amarra e a barra. [<a href="#N2">2</a>] </p>

