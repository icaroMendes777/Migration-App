<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
eu ensinarei para vocês o caminho que é adequado para desenraizar todas as
concepções. Ouçam e prestem muita atenção àquilo que eu vou dizer.” – “Sim,
venerável senhor,” os bhikkhus responderam. </span>O Abençoado disse o
seguinte:<span style='mso-ansi-language:PT-BR'> <span lang=PT-BR><o:p></o:p></span></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual,
bhikkhus, é o caminho adequado para desenraizar todas as concepções? O que
vocês pensam, bhikkhus, o olho é permanente ou impermanente?” – “Impermanente,
venerável senhor.” – “E aquilo que é impermanente é sofrimento ou felicidade?”
– “Sofrimento, venerável senhor.” – “E é adequado considerar o que é
impermanente, sofrimento, sujeito a mudanças como: ‘Isso é meu. Isso sou eu.
Isso é o meu eu’?” – “Não, venerável senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“As formas
são permanentes ou impermanentes? ... A consciência no olho ... O contato no
olho ... Qualquer sensação que surja tendo o contato no olho como condição –
quer seja prazerosa, dolorosa ou nem prazerosa, nem dolorosa – é permanente ou
impermanente? ...<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O ouvido é
permanente ou impermanente? ... A mente ... Qualquer sensação que surja tendo o
contato na mente como condição – quer seja prazerosa, dolorosa ou nem
prazerosa, nem dolorosa – é permanente ou impermanente?” – “Impermanente,
venerável senhor.” – “E aquilo que é impermanente é sofrimento ou felicidade?”
– “Sofrimento, venerável senhor.” – “E é adequado considerar o que é
impermanente, sofrimento, sujeito a mudanças como: ‘Isso é meu. Isso sou eu.
Isso é o meu eu’?” – “Não, venerável senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Vendo
desse modo, o nobre discípulo bem instruído se desencanta com o olho, com as
formas, com a consciência no olho, com o contato no olho, com qualquer sensação
que surja tendo o contato no olho como condição – quer seja prazerosa, dolorosa
ou nem prazerosa, nem dolorosa. Ele se desencanta como o ouvido ... com a mente
... com qualquer sensação que surja tendo o contato na mente como condição ...
Desencantado ele se torna desapegado. Através do desapego a sua mente é
libertada. Quando ela está libertada surge o conhecimento: ‘Libertada.’ Ele
compreende que: ‘O nascimento foi destruído, a vida santa foi vivida, o que
deveria ser feito foi feito, não há mais vir a ser a nenhum estado.’” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Esse,
bhikkhus, é o caminho adequado para desenraizar todas as concepções.”<b
style='mso-bidi-font-weight:normal'><o:p></o:p></b></span></p>


