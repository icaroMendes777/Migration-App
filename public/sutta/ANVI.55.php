<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em certa
ocasião o Abençoado estava próximo a Rajagaha, na Montanha do Pico do Abutre. Naquela ocasião o Venerável Sona estava<span style='mso-spacerun:yes'> 
</span>próximo a Rajagaha no Bosque Frio. Então, quando o Venerável Sona estava
meditando em isolamento [após fazer meditação andando até que a pele da sola
dos pés estivesse cortada e sangrando], este pensamento surgiu na sua mente:
“Dos discípulos do Abençoado que possuem a energia estimulada, eu sou um deles,
porém a minha mente não está libertada das impurezas através do desapego.
Agora, minha família possui riqueza suficiente, sendo possível que eu
desfrutasse da riqueza e ganhasse méritos. Que tal se eu deixasse de lado o
treinamento, retornasse para a vida inferior, desfrutasse da riqueza e ganhasse
mérito?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o Abençoado, assim
que ele percebeu com a sua mente o pensamento na mente do Venerável Sona - como um
homem forte que estende seu braço que está flexionado ou flexiona o braço que
está estendido - desapareceu da Montanha do Pico do Abutre e apareceu no Bosque
Frio em frente ao Venerável Sona, sentando em um assento que havia sido preparado. O
Venerável Sona, após cumprimentar o Abençoado, sentou a um lado e o Abençoado lhe
disse: <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Exatamente agora, enquanto
você meditava em isolamento, este pensamento não surgiu na sua mente: ‘Dos
discípulos do Abençoado que possuem a energia estimulada, eu sou um deles,
porém a minha mente não está libertada das impurezas ... Que tal se eu deixasse
de lado o treinamento, retornasse para a vida comum, desfrutasse da riqueza e
ganhasse mérito?’”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim, senhor.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Agora o que você pensa,
Sona. Antes quando você vivia em família você tinha habilidade para tocar a
vina?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim, senhor.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que você pensa: quando
as cordas da sua vina estavam muito tensas, ela estava afinada e podia ser
tocada?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não, senhor.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que você pensa: quando
as cordas da sua vina estavam muito frouxas, ela estava afinada e podia ser
tocada?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não, senhor.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que você pensa: quando
as cordas da sua vina estavam nem muito tensas nem muito frouxas, mas afinadas
( lit.‘estabelecidas’) no tom exato, sua vina estava afinada e podia ser
tocada?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim, senhor.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Da mesma forma, Sona, o
empenho excessivamente estimulado conduz à inquietação, o empenho
excessivamente frouxo conduz à preguiça. Dessa forma você deve determinar a
afinação exata para o seu empenho, afinar (‘penetrar’, ‘descobrir’) o tom das
(cinco) faculdades, e nesse ponto tomar o seu objeto de meditação.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim, senhor,” respondeu o
Venerável Sona ao Abençoado. Então, tendo dado essa exortação ao Venerável Sona, o
Abençoado - como um homem forte que estende seu braço que está flexionado ou
flexiona o braço que está estendido - desapareceu do Bosque Frio e apareceu na
Montanha do Pico do Abutre. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Permanecendo só, isolado,
diligente, ardente e decidido, em pouco tempo, o Venerável Sona alcançou e
permaneceu no objetivo supremo da vida santa pelo qual membros de um clã deixam
a vida em família pela vida santa, tendo conhecido e realizado por si mesmo no
aqui e agora. Ele soube: “O nascimento foi destruído, a vida santa foi vivida,
o que deveria ser feito foi feito, não há mais vir a ser a nenhum estado.” E
assim o Venerável Sona tornou-se mais um dos Arahants. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, depois de alcançar o
estado de arahant, ao Venerável Sona ocorreu o seguinte pensamento: “E se eu fosse
até o Abençoado e chegando, declarasse o conhecimento na presença dele.” Assim
ele então foi até o Abençoado e ao chegar o cumprimentou e sentou a um lado e
disse para o Abençoado: <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando um bhikkhu é um
arahant, com as impurezas destruidas, que viveu a vida santa, fez o que devia
ser feito, depôs o fardo, alcançou o verdadeiro objetivo, destruiu os grilhões
da existência e está completamente libertado através do conhecimento supremo, ele
se dedica a seis coisas: renúncia, isolamento, não aflição, o fim do desejo,
desapego, e à não delusão. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Agora pode ser que um
certo venerável pense, ‘Talvez seja totalmente na dependência da convicção que
esse venerável se dedica à renúncia,’ mas isso não deve ser visto assim. O
bhikkhu cujas impurezas foram destruidas, que viveu a vida santa, não vê nele
mesmo nada mais para ser feito, ou nada mais para ser adicionado àquilo que foi
feito. É devido ao fim da cobiça, devido a ele estar livre da cobiça, que ele
se dedica à renúncia. É devido ao fim da raiva, devido a ele estar livre da
raiva, que ele se dedica à renúncia. É devido ao fim da delusão, devido a ele
estar livre da delusão, que ele se dedica à renúncia. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Agora pode ser que um
certo venerável pense, ‘Talvez seja porque ele deseja o ganho, honrarias e fama
que esse venerável se dedica à renúncia’. ‘Talvez seja porque ele se apóia
no apego a preceitos e rituais como sendo essenciais que ele se dedica à renúncia’, mas isso não deve ser visto assim. O bhikkhu cujas impurezas foram
destruidas, que viveu a vida santa, não vê nele mesmo nada mais para ser feito,
ou nada mais para ser adicionado àquilo que foi feito. É devido ao fim da
cobiça, devido a ele estar livre da cobiça, que ele se dedica à renúncia’. É
devido ao fim da raiva, devido a ele estar livre da raiva, que ele se dedica à renúncia’. É devido ao fim da delusão, devido a ele estar livre da delusão,
que ele se dedica aà renúncia’. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“É devido ao fim da cobiça,
devido a ele estar livre da cobiça ... É devido ao fim da raiva, devido a ele
estar livre da raiva ... É devido ao fim da delusão, devido a ele estar livre
da delusão que ele se dedica à não aflição ... ao fim do desejo ... ao desapego ... à não
delusão. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mesmo se formas poderosas
percebidas pelo olho aparecessem no campo visual de um bhikkhu cuja mente está
dessa forma libertada, a sua mente não seria nem subjugada nem mesmo engajada.
Permanecendo tranqüilo, tendo alcançado a imperturbabilidade, ele foca na
cessação daquilo. E mesmo se sons poderosos ... aromas ... sabores ...
tangíveis ... Mesmo se objetos mentais poderosos conscientizados pela mente
aparecessem no campo mental de um bhikkhu cuja mente está dessa forma
libertada, a sua mente não seria nem subjugada nem mesmo engajada. Permanecendo
tranqüilo, tendo alcançado a imperturbabilidade, ele foca na cessação daquilo. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Tal como se houvesse uma
montanha com a rocha maciça – sem rachaduras, sem fissuras, uma massa sólida –
e então do leste viesse uma poderosa tempestade com vento e chuva: a montanha
nem tremeria nem se agitaria nem balançaria. E depois do oeste ... do norte ...
do sul<span style='mso-spacerun:yes'>  </span>viesse uma poderosa tempestade
com vento e chuva: a montanha nem tremeria nem se agitaria nem balançaria. Da
mesma forma, mesmo se formas poderosas percebidas pelo olho aparecessem no
campo visual de um bhikkhu cuja mente está dessa forma libertada, a sua mente
não seria nem subjugada nem mesmo engajada. Permanecendo tranqüilo, tendo
alcançado a imperturbabilidade, ele foca na cessação daquilo. E mesmo se sons poderosos...
aromas... sabores... tangíveis... Mesmo se objetos mentais poderosos conscientizados
pela mente aparecessem no campo mental de um bhikkhu cuja mente está dessa
forma libertada, a sua mente não seria nem subjugada nem mesmo engajada.
Permanecendo tranqüilo, tendo alcançado a imperturbabilidade, ele foca na
cessação daquilo.” <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando
a mente está dedicada<br>
&nbsp;&nbsp;&nbsp; à renúncia, isolamento,<br>
&nbsp;&nbsp;&nbsp; não aflição, o fim do desejo,<br>
&nbsp;&nbsp;&nbsp; o desapego e à não delusão,<br>
ao ver o surgimento nos meios dos sentidos,<br>
&nbsp;&nbsp;&nbsp; a mente está corretamente libertada.<br>
Para esse bhikkhu, corretamente libertado,<br>
&nbsp;&nbsp;&nbsp; com o coração em paz,<br>
&nbsp;&nbsp;&nbsp; não há nada mais para ser feito,<br>
&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp; nada que adicionar<br>
&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp; àquilo que foi feito.<br>
Como uma rocha maciça que não se abala com o vento,<br>
assim mesmo todas as formas, sabores, sons,<br>
&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp; aromas, tangíveis,<br>
&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp; objetos mentais desejáveis ou não,<br>
&nbsp;&nbsp;&nbsp; não têm efeito naquele que é Tatha.[<a href="#N1">1</a>]<br>
&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp; <a name=R1></a>A mente – tranqüila,
totalmente libertada - <br>
&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp; foca na cessação daquilo. <o:p></o:p></span></p>

