<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[2] Este trecho mostra de forma clara e explícita a regra definida pelo
Buda para a Sangha sobre o consumo de carne. Pode ser notado que o Buda não
exige que os bhikkhus observem uma dieta vegetariana, mas permite o consumo de
carne quando houver certeza de que o animal não tenha sido sacrificado
especialmente para aquela refeição. Essa carne é chamada de <i>tikotiparisuddha</i>,
“pura em três aspectos,” porque não foi visto, ouvido ou suspeitado que o
animal tenha sido sacrificado para o bhikkhu. O preceito Budista leigo de se
abster de matar proíbe matar para comer mas não proíbe comprar carne de animais
que já foram sacrificados. [</span><a href="#R2"><span lang=PT-BR
style='mso-ansi-language:PT-BR'>Retorna</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=N3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[3] Aqui o Buda mostra que ele não só permanece com amor bondade através
da supressão da má vontade por meio do jhana baseado no amor bondade, como
ocorre com a divindade Brahma, mas que ele erradicou as raízes da má vontade
por meio da realização do estado de arahant. [</span><a href="#R3"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>Retorna</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>



<p class=Normal><a name=N4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[4] Crueldade, descontentamento e aversão, (<i>vihesa, arati, patigha</i>), são os opostos
de compaixão, alegria altruísta e equanimidade respectivamente. [</span><a
href="#R4"><span lang=PT-BR style='mso-ansi-language:PT-BR'>Retorna</span></a><span
