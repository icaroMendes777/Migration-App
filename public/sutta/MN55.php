<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] Jivaka era o filho abandonado por uma cortesã. Encontrado e educado pelo
Príncipe Abhaya, ele estudou medicina em Takkasila e mais tarde foi nomeado o
médico pessoal do Buda. Ele alcançou o estado de entrar na correnteza depois de
ouvir um discurso do Buda. [</span><a href="#R1"><span lang=PT-BR
style='mso-ansi-language:PT-BR'>Retorna</span></a><span lang=PT-BR
