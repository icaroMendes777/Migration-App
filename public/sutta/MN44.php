<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim
ouvi. Em certa ocasião o Abençoado estava em Rajagaha, no Bambual, no Santuário
dos Esquilos. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, o
discípulo leigo Visakha foi até a bhikkhuni Dhammadinna, [<a href="#N1">1</a>] <a
name=R1></a>e após cumprimentá-la, sentou a um lado e perguntou: <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(Identidade)</b><o:p></o:p></span></p>



<p class=Normal><a name=T2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2. &quot;Senhora, 'identidade, identidade' se diz. O que é<span
style="mso-spacerun: yes">  </span>chamado identidade pelo Abençoado?&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Amigo
Visakha, esses cinco agregados influenciados pelo apego são chamados de
identidade pelo Abençoado; isto é, o agregado da forma material influenciado
pelo apego, o agregado da sensação influenciado pelo apego, o agregado da
percepção influenciado pelo apego, o agregado das formações volitivas influenciado pelo
apego e o agregado da consciência influenciado pelo apego. Esses cinco
agregados influenciados pelo apego são chamados de identidade pelo
Abençoado.&quot;[<a href="#N2">2</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Dizendo, &quot;Muito bem, senhora&quot;, o discípulo leigo Visakha ficou
alegre e contente com as palavras da bhikkhuni Dhammadinna. Em seguida ele fez
outra pergunta: <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3.
&quot;Senhora, 'origem da identidade, origem da identidade' se diz. O que
é<span style="mso-spacerun: yes">  </span>chamado de origem da identidade pelo
Abençoado?&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Amigo
Visakha, é o desejo, que conduz à renovação dos seres, que é acompanhado pela cobiça e pelo prazer, 
buscando o prazer aqui e ali; isto é, o desejo pelos prazeres
sensuais, o desejo por ser/existir e o desejo por não ser/existir. Isso é chamado
de origem da identidade pelo Abençoado.&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4.
&quot;Senhora, 'cessação da identidade, cessação da identidade' se diz. O que
é<span style="mso-spacerun: yes">  </span>chamado de cessação da identidade
pelo Abençoado?&quot; <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Amigo
Visakha, é o desaparecimento e cessação sem deixar nenhum vestígio 
daquele mesmo desejo, abrir mão, descartar, libertar-se, despegar desse mesmo desejo. Isso é chamado de cessação da
identidade pelo Abençoado.&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5.
&quot;Senhora, 'o caminho que conduz à cessação da identidade, o caminho que
conduz à cessação da identidade' se diz. O que é<span style="mso-spacerun:
yes">  </span>chamado de caminho que conduz à cessação da identidade pelo
Abençoado?&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Amigo
Visakha, é exatamente este Nobre Caminho Óctuplo; isto é, entendimento correto,
pensamento correto, linguagem correta, ação correta, modo de vida correto,
esforço correto, atenção plena correta e concentração correta.&quot;<o:p></o:p></span></p>



<p class=Normal><a name=T6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>6. “Senhora, esse apego é o mesmo que os cinco agregados influenciados
pelo apego ou esse apego é algo separado dos cinco agregados influenciados pelo
apego?&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Amigo
Visakha, esse apego não é nem o mesmo que esses cinco agregados influenciados
pelo apego, nem esse apego é algo separado dos cinco agregados influenciados
pelo apego. É o desejo e a cobiça em relação aos cinco agregados influenciados
pelo apego que é o apego nesse caso.&quot; [<a href="#N3">3</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'><b>(Idéia da Identidade)</b><o:p></o:p></span></p>



<p class=Normal><a name=T7></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>7. &quot;Senhora, como surge a idéia da identidade?&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Nesse
caso, amigo Visakha, uma pessoa comum sem instrução, que não respeita os
nobres, que não é proficiente nem treinada no Dhamma deles, que não
respeita os homens verdadeiros, que não é proficiente nem treinada no
Dhamma deles, considera a forma material como sendo o eu, ou o eu como possuído
de forma material, ou a forma material como estando no eu, ou o eu como estando
na forma material . Ela considera a sensação como sendo o eu, ou o eu como
possuído de sensação, ou a sensação como estando no eu, ou o eu como estando na
sensação. Ela considera a percepção como sendo o eu, ou o eu como possuído de
percepção, ou a percepção como estando no eu, ou o eu como estando na
percepção. Ela considera as formações volitivas como sendo o eu, ou o eu como possuído de
formações volitivas, ou as formações volitivas como estando no eu, ou o eu como estando nas
formações volitivas. Ela considera a consciência como sendo o eu, ou o eu como possuído
de consciência, ou a consciência como estando no eu, ou o eu como estando na
consciência. Assim é como surge a idéia da identidade.&quot; [<a href="#N4">4</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R4></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>8. &quot;Senhora, como não surge a idéia da identidade?&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Nesse
caso, amigo Visakha, um nobre discípulo bem instruído, que respeita os nobres,
que é proficiente e treinado no Dhamma deles, que respeita os homens
verdadeiros, que é proficiente e treinado no Dhamma deles, não considera a
forma material como sendo o eu, ou o eu como possuído de forma material, ou a
forma material como estando no eu, ou o eu como estando na forma material . Ela
não considera a sensação como sendo o eu, ou o eu como possuído de sensação, ou
a sensação como estando no eu, ou o eu como estando na sensação. Ela não
considera a percepção como sendo o eu, ou o eu como possuído de percepção, ou a
percepção como estando no eu, ou o eu como estando na percepção. Ela não
considera as formações volitivas como sendo o eu, ou o eu como possuído de formações volitivas, ou
as formações volitivas como estando no eu, ou o eu como estando nas formações volitivas. Ela não
considera a consciência como sendo o eu, ou o eu como possuído de consciência,
ou a consciência como estando no eu, ou o eu como estando na consciência. Assim
é como não surge a idéia da identidade.&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>( O Nobre
Caminho Óctuplo) </b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9.
&quot;Senhora, o que é o Nobre Caminho Óctuplo?&quot; <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Amigo
Visakha, é exatamente este Nobre Caminho Óctuplo; isto é, entendimento correto,
pensamento correto, linguagem correta, ação correta, modo de vida correto,
esforço correto, atenção plena correta e concentração correta.&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10.
&quot;Senhora, o Nobre Caminho Óctuplo é condicionado ou não
condicionado?&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Amigo
Visakha, o Nobre Caminho Óctuplo é condicionado.&quot;</span><span lang=PT-BR
style='font-size:11.0pt;mso-bidi-font-size:12.0pt;mso-ansi-language:PT-BR'><o:p></o:p></span></p>



<p class=Normal><a name=T11></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>11. &quot;Senhora, os três agregados estão incluídos no Nobre Caminho
Óctuplo, ou o Nobre Caminho Óctuplo está incluído nos três agregados?&quot; [<a
href="#N5">5</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R5></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>&quot;Os três agregados não estão incluídos no Nobre Caminho Óctuplo,
amigo Visakha, mas o Nobre Caminho Óctuplo está incluído nos três agregados.
Linguagem correta, ação correta e modo de vida correto - esses estados estão
incluídos no agregado da virtude. Esforço correto, atenção plena correta e
concentração correta - esses estados estão incluídos no agregado da
concentração. Entendimento correto e pensamento correto - esses estados estão
incluídos no agregado da sabedoria.&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(Concentração)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12.
&quot;Senhora, o que é concentração? Qual é a base da concentração? Qual é o
equipamento da concentração? Qual é o desenvolvimento da concentração?&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Unificação
da mente, amigo Visakha, é concentração; os quatro fundamentos da atenção plena
são a base da concentração; os quatro tipos de esforço constituem o equipamento
da concentração; a repetição, o desenvolvimento e o cultivo desses mesmos
estados constituem o desenvolvimento da concentração.&quot; [<a href="#N6">6</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R6></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'><b>(Formações)</b><o:p></o:p></span></p>



<p class=Normal><a name=T13></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>13. &quot;Senhora, quantas formações existem?&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Existem
três formações, amigo Visakha: a formação corporal, a formação verbal e a
formação mental.&quot; </span><span lang=PT-BR style='font-size:11.0pt;
mso-bidi-font-size:12.0pt;mso-ansi-language:PT-BR'><o:p></o:p></span></p>



<p class=Normal><a name=T14></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>14. “Mas, senhora, o que é a formação corporal? O que é a formação
verbal? O que é a formação mental?&quot;<span style="mso-spacerun: yes"> 
</span><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;A
inspiração e expiração, amigo Visakha, são a formação corporal; pensamento aplicado
e pensamento sustentado são a formação verbal; percepção e sensação são a
formação mental.&quot; [<a href="#N7">7</a>] <o:p></o:p></span></p>



<p class=Normal><a name=R7></a><a name=T15></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>15. &quot;Mas, senhora, porque a inspiração e a
expiração são a formação corporal? Porque o pensamento aplicado e o pensamento
sustentado são a formação verbal? Porque a percepção e a sensação são a
formação mental.&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Amigo
Visakha, a inspiração e a expiração são corporais, são estados inseparavelmente
conectados com o corpo; é por isso que a inspiração e a expiração são a formação
corporal. Primeiro a pessoa aplica o pensamento e sustenta o pensamento e em
seguida emerge a linguagem; é por isso que o pensamento aplicado e o pensamento
sustentado são a formação verbal. A percepção e a sensação são mentais, são
estados inseparavelmente conectados com a mente; é por isso que a percepção e a
sensação são a formação mental.&quot; [<a href="#N8">8</a>]<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(A
Realização da Cessação)</b><o:p></o:p></span></p>



<p class=Normal><a name=R8></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>16. &quot;Senhora, como ocorre a realização da cessação da percepção e
da sensação?&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Amigo
Visakha, quando um bhikkhu está realizando a cessação da percepção e da
sensação, não lhe ocorre: 'Eu irei realizar a cessação da percepção e da
sensação,' ou 'Eu estou realizando a cessação da percepção e da sensação,' ou
'Eu realizei a cessação da percepção e da sensação'; mas, ao invés disso, a sua
mente já foi previamente desenvolvida de tal forma a conduzi-lo a esse
estado.&quot; [<a href="#N9">9</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R9></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>17. &quot;Senhora, quando um bhikkhu está realizando a cessação da
percepção e da sensação, quais estados cessam primeiro: a formação
corporal,<span style="mso-spacerun: yes">  </span>a formação verbal ou a
formação mental?&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Amigo
Visakha, quando um bhikkhu está realizando a cessação da percepção e da
sensação, primeiro cessa a formação verbal, depois a formação corporal, depois
a formação mental.&quot; [<a href="#N10">10</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R10></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>18. &quot;Senhora, como ocorre a emersão da realização da cessação da
percepção e da sensação?&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Amigo
Visakha, quando um bhikkhu está emergindo da realização da cessação da
percepção e da sensação, não lhe ocorre: 'Eu irei emergir da realização da
cessação da percepção e da sensação,' ou 'Eu estou emergindo da realização da
cessação da percepção e da sensação,' ou 'Eu emergi da realização da cessação
da percepção e da sensação'; mas, ao invés disso, a sua mente que foi
previamente desenvolvida o conduz a esse estado.&quot; [<a href="#N11">11</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R11></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>19. &quot;Senhora, quando um bhikkhu está emergindo da realização da
cessação da percepção e da sensação, quais estados emergem primeiro: a formação
corporal,<span style="mso-spacerun: yes">  </span>a formação verbal ou a
formação mental?&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Amigo
Visakha, quando um bhikkhu está emergindo da realização da cessação da
percepção e da sensação, primeiro emerge a formação mental, depois a formação
corporal, depois a formação verbal.&quot; [<a href="#N12">12</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R12></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>20. &quot;Senhora, quando um bhikkhu emergiu da realização da cessação
da percepção e da sensação, quantos tipos de contato o tocam?&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Amigo
Visakha, quando um bhikkhu emergiu da realização da cessação da percepção e da sensação,
três tipos de contato o tocam: contato vazio, contato sem sinal, contato sem
desejo.&quot; [<a href="#N13">13</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R13></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>21. &quot;Senhora, quando um bhikkhu emergiu da realização da cessação
da percepção e da sensação, para que direção tende a sua mente, em que direção
se inclina, em que se apóia?&quot; <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Amigo
Visakha, quando um bhikkhu emergiu da realização da cessação da percepção e da
sensação, a sua mente tende para o afastamento, se inclina para o afastamento,
se apóia no afastamento.&quot; [<a href="#N14">14</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R14></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'><b>(Sensação)</b><o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>22.
&quot;Senhora, quantos tipos de sensação existem?&quot; <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Amigo
Visakha, existem três tipos de sensação: sensação prazerosa, sensação desprazerosa
e sensação nem prazerosa, nem desprazerosa.&quot; <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>23.
&quot;Mas senhora, o que é a sensação prazerosa? O que é a sensação desprazerosa? O
que é a sensação nem prazerosa, nem desprazerosa?&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Amigo
Visakha, tudo que é sentido pelo corpo ou pela mente como agradável e que
alivia é uma sensação agradável. Tudo que é sentido pelo corpo ou pela mente
como doloroso e que magoa é uma sensação desagradável. Tudo que é sentido pelo
corpo ou pela mente que não alivia nem magoa é uma sensação nem prazerosa, nem
desprazerosa.&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>24.
&quot;Senhora, o que é prazeroso e o que é doloroso em relação à sensação
prazerosa? O que é prazeroso e o que é doloroso em relação à sensação desprazerosa?
O que é prazeroso e o que é doloroso em relação à sensação nem prazerosa, nem
desprazerosa?&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Amigo
Visakha, a sensação prazerosa é prazerosa quando ela persiste e desprazerosa quando
muda. A sensação desprazerosa é desprazerosa quando persiste e prazerosa quando muda. A
sensação nem prazerosa, nem desprazerosa é prazerosa quando existe o conhecimento
(dela) e desprazerosa quando não existe o conhecimento (dela).&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(Tendências
Subjacentes)</b><o:p></o:p></span></p>



<p class=Normal><a name=T25></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>25. &quot;Senhora, quais tendências subjacentes estão por detrás da
sensação prazerosa? Quais tendências subjacentes estão por detrás da sensação
desprazerosa? Quais tendências subjacentes estão por detrás da sensação nem
