<p class=Normal> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: "Bhikkhus, aquele que não tem o conhecimento direto  e a compreensão completa do desdém, que não desapegou a sua mente disso e abandonou isso, será incapaz de dar um fim ao sofrimento. Mas aquele que tem o conhecimento direto e a compreensão completa do desdém, que desapegou a sua mente disso e abandonou isso, será capaz de de dar um fim ao sofrimento."</p>

<p class=MsoNormal style='margin-left:.75in'> Os seres estão tomados pelo desdém,<br>
atados pelo desdém e deliciando-se com a existência;<br>
sem compreender completamente o desdém,<br>
renascem repetidas vezes.</p>

<p class=MsoNormal style='margin-left:.75in'> Mas aqueles que abandonaram o desdém,<br>
libertados tendo destruído o desdém,<br>
tendo vencido o grilhão do desdém,<br>
superaram todo sofrimento.</p>


