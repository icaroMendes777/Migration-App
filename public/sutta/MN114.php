<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3.
“Bhikkhus;[<a href="#N1">1</a>] <a name=R1></a>a conduta corporal é de dois
tipos, eu digo: para ser cultivada e para não ser cultivada. E a conduta
corporal ou é uma, ou é a outra. [<a href="#N2">2</a>] <a name=R2></a>A conduta
verbal é de dois tipos, eu digo: para ser cultivada e para não ser cultivada. E
a conduta verbal ou é uma, ou é a outra. A conduta mental é de dois tipos, eu
digo: para ser cultivada e para não ser cultivada. E a conduta mental ou é uma,
ou é a outra. A inclinação da mente é de dois tipos, eu digo: para ser
cultivada e para não ser cultivada. E a inclinação da mente ou é uma, ou é a
outra. A obtenção da percepção é de dois tipos, eu digo: para ser cultivada e
para não ser cultivada. E a obtenção da percepção ou é uma, ou é a outra. A
obtenção do entendimento é de dois tipos, eu digo: para ser cultivada e para
não ser cultivada. E a obtenção do entendimento ou é uma, ou é a outra. A
obtenção da individualidade é de dois tipos, eu digo: para ser cultivada e para
não ser cultivada. E a obtenção da individualidade ou é uma, ou é a
outra.&quot;<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(PRIMEIRA
