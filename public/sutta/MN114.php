PT-BR'>:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.
“Bhikkhus, eu ensinarei aquilo que deve ser cultivado e aquilo que não deve ser
cultivado. </span>Ouçam e prestem muita atenção ao que vou dizer.” –
“Sim, venerável senhor,” os bhikkhus responderam. O Abençoado disse o seguinte<span
lang=PT-BR style='mso-ansi-language:PT-BR'>:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(PRIMEIRA
