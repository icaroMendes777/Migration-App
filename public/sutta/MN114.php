Assim foi dito pelo Abençoado. E com referência a que foi dito isso?<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, aquela conduta verbal que faz com que os estados prejudiciais aumentem
e os estados benéficos diminuam naquele que a cultiva, não deve ser cultivada.
Mas aquela conduta verbal que faz com que os estados prejudiciais diminuam e os
estados benéficos aumentem naquele que a cultiva, deve ser cultivada.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual é o
tipo de conduta verbal que faz com que os estados prejudiciais aumentem e os
estados benéficos diminuam naquele que a cultiva? Aqui, alguém emprega
linguagem mentirosa; tendo sido chamado para uma corte, uma reunião, um
encontro com seus parentes, com a sua corporação, com a família real, se assim
for questionado como testemunha: 'Então, bom homem, diga o que você sabe,' se
ele não souber, dirá, 'Eu sei'; se ele souber, dirá, 'Eu não sei'; se ele não
viu, dirá, 'Eu vi'; se ele viu, dirá, 'Eu não vi'; com plena consciência ele
conta mentiras em seu próprio benefício, pelo benefício de outros, ou para
obter algum benefício mundano insignificante. Ele emprega linguagem maliciosa;
o que ouviu aqui ele conta ali para separar aquelas pessoas destas, ou o que
ele ouviu lá, conta aqui para separar estas pessoas daquelas; assim ele separa
aquelas pessoas que estão unidas, ele cria divisões, ama a discórdia, se
delicia com a discórdia, desfruta da discórdia, diz coisas que criam a
discórdia. Ele emprega linguagem grosseira; ele emprega palavras que são
grosseiras, duras, que magoam os outros, que ofendem os outros, próximas da
raiva e que não favorecem a concentração. Ele emprega a linguagem frívola; ele
fala fora de hora, diz o que não é fato, diz o que é inútil, diz aquilo que é
contrário ao Dhamma e Disciplina; nas horas inadequadas ele diz palavras que
são inúteis, irracionais, imoderadas e que não trazem benefício. Esse tipo de
conduta verbal faz com que os estados prejudiciais aumentem e os estados
benéficos diminuam naquele que a cultiva.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual é o
tipo de conduta verbal que faz com que os estados benéficos aumentem e os
estados prejudiciais diminuam naquele que a cultiva? Aqui, alguém, abandonando
a linguagem mentirosa, se abstém da linguagem mentirosa; tendo sido chamado
para uma corte, uma reunião, um encontro com seus parentes, com a sua
corporação, com a família real, se assim for questionado como testemunha:
'Então, bom homem, diga o que você sabe,' se ele não souber, dirá, 'Eu não
sei'; se ele souber, dirá, 'Eu sei'; se ele não viu, dirá, 'Eu não vi'; se ele
viu, dirá, 'Eu vi'. Assim, com plena consciência ele não conta mentiras em seu
próprio benefício, pelo benefício de outros, ou para obter algum benefício
mundano insignificante. Abandonando a linguagem maliciosa, ele se abstém da
linguagem maliciosa; o que ouviu aqui ele não conta ali para separar aquelas
pessoas destas, ou, o que ouviu lá ele não conta aqui para separar estas
pessoas daquelas; assim, ele reconcilia aquelas pessoas que estão divididas,
promove a amizade, ele ama a concórdia, se delicia com a concórdia, desfruta da
concórdia, diz coisas que criam a concórdia. Abandonando a linguagem grosseira,
ele se abstém da linguagem grosseira. Ele diz palavras que são gentis, que
agradam aos ouvidos, carinhosas, que penetram o coração, que são corteses,
desejadas por muitos e que agradam a muitos. Abandonando a linguagem frívola,
ele se abstém da linguagem frívola. Ele fala na hora certa, diz o que é fato,
aquilo que é bom, fala de acordo com o Dhamma e a Disciplina; nas horas
adequadas ele diz palavras que são úteis, racionais, moderadas e que trazem
benefício. Esse tipo de conduta verbal faz com que os estados benéficos
aumentem e os estados prejudiciais diminuam naquele que a cultiva.<o:p></o:p></span></p>



