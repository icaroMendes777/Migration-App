<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual é o
tipo de conduta mental que faz com que os estados prejudiciais aumentem e os
estados benéficos diminuam naquele que a cultiva? Aqui, alguém é cobiçoso; ele
cobiça os bens e propriedades dos outros, pensando, 'Ah, que aquilo que
pertence aos outros seja meu!' Ou a sua mente possui má vontade e as suas
intenções estão plenas de raiva: 'Que esses seres sejam mortos e assassinados,
que eles cessem, faleçam ou sejam aniquilados!'<span style='mso-spacerun:yes'> 
</span>Esse tipo de conduta mental faz com que os estados prejudiciais aumentem
