<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. Quando
isso foi dito, o venerável Sariputta disse para o Abençoado: “Venerável senhor,
eu entendo o significado em detalhe da afirmação do Abençoado, que foi dita de
modo resumido sem explicar o significado em detalhe, da seguinte forma:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5.
“’Bhikkhus; a conduta corporal pode ser de dois tipos, eu digo: para ser
cultivada e para não ser cultivada. E a conduta corporal ou é uma, ou é a
outra.’ Assim foi dito pelo Abençoado. E com referência a que foi dito isso?<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, aquela conduta corporal que faz com que os estados prejudiciais
aumentem e os estados benéficos diminuam naquele que a cultiva, não deve ser
cultivada. Mas aquela conduta corporal que faz com que os estados prejudiciais
diminuam e os estados benéficos aumentem naquele que a cultiva, deve ser
cultivada.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual é o
tipo de conduta corporal que faz com que os estados prejudiciais aumentem e os
estados benéficos diminuam naquele que a cultiva? Aqui, alguém tira a vida de
outros seres, um homicida, sanguinário, dedicado a golpes e violência,
demonstrando nenhuma piedade com os seres vivos. Ele toma o que não é dado, ele
toma, como se fosse um ladrão, os bens e propriedades de outros num vilarejo ou
na floresta. Ele se comporta de forma imprópria em relação aos prazeres
sensuais; ele se envolve sexualmente com quem está sob a proteção da mãe, do
pai, dos irmãos, das irmãs, dos parentes, que possui esposo, protegida pela lei
ou mesmo com quem esteja coroada de flores por um outro homem. Esse tipo de
conduta corporal faz com que os estados prejudiciais aumentem e os estados
benéficos diminuam naquele que a cultiva. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual é o
tipo de conduta corporal que faz com que os estados benéficos aumentem e os
estados prejudiciais diminuam naquele que a cultiva? Aqui, alguém abandonando
tirar a vida de outros seres, se abstém de tirar a vida de outros seres; ele
permanece com a sua vara e arma postas de lado, bondoso e gentil, compassivo com
todos os seres vivos. Abandonando tomar o que não seja dado, ele se abstém de
tomar o que não é dado; ele não toma, como se fosse um ladrão, os bens e
propriedades de outros num vilarejo ou na floresta. Abandonando a conduta
imprópria com relação aos prazeres sensuais, ele se abstém da conduta imprópria
com relação aos prazeres sensuais; ele não se envolve sexualmente com quem está
sob a proteção da mãe, do pai, dos irmãos, das irmãs, dos parentes, que possuem
esposo, protegidas pela lei ou mesmo com quem esteja coroada de flores por um
outro homem. Esse tipo de conduta corporal faz com que os estados benéficos
aumentem e os estados prejudiciais diminuam naquele que a cultiva.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
foi com referência a isso que foi dito pelo Abençoado: ‘Bhikkhus; a conduta
corporal pode ser de dois tipos, eu digo: para ser cultivada e para não ser
cultivada. E a conduta corporal ou é uma, ou é a outra.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6.
“’Bhikkhus; a conduta verbal pode ser de dois tipos, eu digo: para ser
