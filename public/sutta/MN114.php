<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual é o
tipo de conduta mental que faz com que os estados benéficos aumentem e os
estados prejudiciais diminuam naquele que a cultiva? Aqui, alguém não é
cobiçoso. Ele não cobiça as posses dos outros, pensando, 'Ah, que aquilo que
pertence aos outros seja meu!' A sua mente não possui má vontade e as suas
intenções estão isentas de raiva: 'Que esses seres possam estar livres da
inimizade, aflição e ansiedade! Que eles vivam felizes!’ Esse tipo de conduta
mental faz com que os estados benéficos aumentem e os estados prejudiciais
diminuam naquele que a cultiva.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
