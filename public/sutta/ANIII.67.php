<p class=Normal> “Bhikkhus, há esses três tópicos para conversação. Quais três? </p>

<p class=Normal>Alguém pode falar sobre o passado, dizendo, ‘Assim foi no passado.’ Alguém pode falar sobre o futuro, dizendo, ‘Assim será no futuro.’ Ou alguém pode falar sobre agora no presente, dizendo, ‘Assim é agora no presente.’ </p>

<p class=Normal> “Bhikkhus, é através da forma como ela participa numa conversa que uma pessoa pode ser conhecida como adequada para conversar ou inadequada para conversar. Se uma pessoa, ao ser perguntada, não dá uma resposta categórica para uma pergunta que merece uma resposta categórica, não dá uma resposta analítica a uma pergunta que merece uma resposta analítica, não faz uma contra-pergunta para uma pergunta que merece uma contra-pergunta, não deixa de lado uma pergunta que merece ser deixada de lado, então, em sendo esse o caso, ela é uma pessoa inadequada para conversar. Mas se uma pessoa, ao ser perguntada, dá uma resposta categórica para uma pergunta que merece uma resposta categórica, dá uma resposta analítica a uma pergunta que merece uma resposta analítica, faz uma contra-pergunta para uma pergunta que merece uma contra-pergunta, deixa de lado uma pergunta que merece ser deixada de lado, então, em sendo esse o caso, ela é uma pessoa adequada para conversar. </p>

<p class=Normal> “Bhikkhus, é através da forma como ela participa numa conversa que uma pessoa pode ser conhecida como adequada para conversar ou inadequada para conversar. Se uma pessoa, ao ser perguntada, não sustenta aquilo que é possível e impossível, não sustenta as premissas que foram acordadas, não sustenta os ensinamentos que são conhecidos como verdadeiros, [<a href="#N1">1</a>] <a name="R1"> </a>
não sustenta o procedimento padrão, em sendo esse o caso, ela é uma pessoa inadequada para conversar. Mas se uma pessoa, ao ser perguntada, sustenta aquilo que é possível e impossível, sustenta as premissas que foram acordadas, sustenta os ensinamentos que são conhecidos como verdadeiros, sustenta o procedimento padrão, em sendo esse o caso, ela é uma pessoa adequada para conversar. </p>

<p class=Normal> “Bhikkhus, é através da forma como ela participa numa conversa que uma pessoa pode ser conhecida como adequada para conversar ou inadequada para conversar. Se uma pessoa, ao ser perguntada, se desvia de uma coisa para outra, muda o assunto da conversa, demonstra raiva, aversão e aborrecimento, então, em sendo esse o caso, ela é uma pessoa inadequada para conversar. Mas se uma pessoa, ao ser perguntada, não se desvia de uma coisa para outra, não muda o assunto da conversa, não demonstra raiva, aversão e aborrecimento, então, em sendo esse o caso, ela é uma pessoa adequada para conversar. </p>

<p class=Normal> “Bhikkhus, é através da forma como ela participa numa conversa que uma pessoa pode ser conhecida como adequada para conversar ou inadequada para conversar. Se uma pessoa, ao ser perguntada, humilha [quem faz a pergunta], ofende, ridiculariza, se agarra aos pequenos erros dela, então, em sendo esse o caso, ela é uma pessoa inadequada para conversar.  Mas se uma pessoa, ao ser perguntada, não humilha [quem faz a pergunta], não ofende, não ridiculariza, não se agarra aos pequenos erros dela, então, em sendo esse o caso, ela é uma pessoa adequada para conversar. </p>

<p class=Normal> “Bhikkhus, é através da forma como ela participa numa conversa que uma pessoa pode ser conhecida como aquela que se aproxima ou que não se aproxima. Aquela que dá ouvidos se aproxima; aquela que não dá ouvidos não se aproxima. Ao se aproximar, ela compreende completamente uma qualidade, ela abandona uma qualidade, ela realiza uma qualidade, ela desenvolve uma qualidade. [<a href="#N2">2</a>] <a name="R2"> </a>Compreendendo completamente uma qualidade, abandonando uma qualidade, realizando uma qualidade, desenvolvendo uma qualidade, ela realiza a libertação correta. Pois esse é o propósito das conversas, esse é o propósito do aconselhamento, esse é o propósito de se aproximar, esse é o propósito de dar ouvidos, isto é, a libertação da mente através do desapego. </p>

<p class=Normal style='margin-left:.75in'>Aqueles que conversam<br>
quando enraivecidos, dogmáticos, arrogantes,<br>
	&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp; seguindo aquilo que não é o modo nobre, <br>
	&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;buscando expor os defeitos dos outros, <br>
deliciando-se com<br>		
&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;a fala incorreta, <br>
			&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;deslizes, tropeções, derrotas, dos outros. <br>
	Os nobres<br>
	não falam assim. </p>

<p class=Normal style='margin-left:.75in'>Se os nobres, sabendo o momento apropriado,
querem falar, <br>
então, palavras conectadas com o justo, <br>
de acordo com o modo dos nobres: <br>
isso é o que os iluminados dizem, <br>
sem raiva ou arrogância, <br>
com a mente sem agitação, <br>
sem veemência, sem maldade, <br>
sem inveja,<br>
	&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;eles falam com base no entendimento correto. <br>
Eles se deliciam com aquilo que é bem dito<br>
e não depreciam aquilo que não é. <br>
Eles não estudam para encontrar defeitos, <br>
não se agarram aos pequenos erros. <br>
Não humilham, não ofendem, <br>
não dizem palavras à toa.</p>

<p class=Normal style='margin-left:.75in'>Tendo como objetivo o conhecimento, <br>
tendo como objetivo inspirar confiança, <br>
conselhos que são verdadeiros: <br>
	&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;assim os nobres aconselham, <br>
	&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;esse é o conselho dos nobres. <br>
Sabendo disso, o nobre<br>
deve aconselhar sem arrogância.” <br>

