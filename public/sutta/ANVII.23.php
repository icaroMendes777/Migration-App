<p class=Normal>Certa ocasoão o Abençoado estava em Rajagaha na Montanha do Pico do Abutre. Lá ele se dirigiu aos bhikkhus: “Bhikkhus!" </p>

<p class=Normal> "Venerável senhor!" os bhikkhus responderam. O Abençoado disse o seguinte: </p>

<p class=Normal> "Bhikkhus, eu lhes ensinarei os sete princípios do não-declínio. Ouçam e prestem muita atenção àquilo que eu vou dizer." </p>

<p class=Normal>"Sim, venerável senhor," aqueles bhikkhus responderam. O Abençoado disse o seguinte: </p>

<p class=Normal>"Quais, bhikkhus, são os sete princípios do não-declínio? </p>

<p class=Normal> (1) "Enquanto os bhikkhus se reunirem em assembléias com freqüência e regularmente, é de se esperar que eles prosperem e não que declinem. </p>

<p class=Normal> (2) "Enquanto os bhikkhus se reunirem em harmonia, se separarem em harmonia e conduzirem os seus negócios da Sangha em harmonia, é de se esperar que eles prosperem e não que declinem.</p>

<p class=Normal> (3) "Enquanto os bhikkhus não permitirem aquilo que ainda não foi autorizado e
não abolirem aquilo que foi autorizado, mas prosseguirem de acordo com
aquilo que foi autorizado nas regras de treinamento, é de se esperar que eles prosperem e não que declinem.</p>

<p class=Normal> (4) "Enquanto os bhikkhus honrarem,
respeitarem, reverenciarem e saudarem os bhikkhus seniores que foram ordenados
há muito tempo, os pais e líderes da ordem, é de se esperar que eles prosperem e não que declinem.</p>

<p class=Normal> (5) "Enquanto os bhikkhus não forem uma
presa dos desejos que neles surgirem e que conduzem ao renascimento, é de se esperar que eles prosperem e não que declinem.</p>

<p class=Normal> (6) "Enquanto os bhikkhus se dedicarem a viver nas florestas, é de se esperar que eles prosperem e não que declinem.</p>

<p class=Normal> (7) "Enquanto os bhikkhus mantiverem
a sua atenção plena estabelecida, para que no futuro os bons companheiros
venham até eles, e aqueles que já vieram se sintam bem na companhia deles, é de se esperar que eles prosperem e não que declinem.</p>

<p class=Normal>"Bhikkhus, enquanto esses sete princípios do não-declínio persistirem entre os bhikkhus, e os bhikkhus forem vistos estarem neles estabelecidos, eles apenas devem esperar desenvolvimento, não declínio." </p>


