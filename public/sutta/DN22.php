<p class=Normal><a name=T36></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>36. &quot;
E como, bhikkhus, um bhikkhu permanece contemplando os objetos mentais como
objetos mentais? [</span><a href="#N25">25</span></a><span lang=PT-BR style='mso-ansi-language:
