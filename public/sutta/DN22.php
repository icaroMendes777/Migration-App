<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. &quot; Bhikkhus, este é
o caminho direto [</span><a href="#N3">3</span></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>] <a name=R3></a>para a purificação dos seres, para superar a tristeza e
a lamentação, para o desaparecimento da dor e da angústia, [<a href="#N3a">3a</a>]
<a name=R3a></a>para alcançar o caminho verdadeiro, para a realização de
Nibbana – isto é, os quatro fundamentos da atenção plena.[</span><a href="#N4">4</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3.<a name=R4></a> &quot;
Quais são os quatro? Aqui, bhikkhus, um bhikkhu[</span><a href="#N5">5</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R5></a>permanece
contemplando o corpo como um corpo, ardente, plenamente consciente e com
atenção plena, tendo colocado de lado a cobiça e o desprazer pelo mundo.[</span><a
href="#N6">6</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R6></a>Ele permanece
contemplando as sensações como sensações, ardente, plenamente consciente e com
atenção plena, tendo colocado de lado a cobiça e o desprazer pelo mundo. Ele
permanece contemplando a mente como mente, ardente, plenamente consciente e com
atenção plena, tendo colocado de lado a cobiça e o desprazer pelo mundo. Ele
permanece contemplando os objetos mentais como objetos mentais, ardente,
plenamente consciente e com atenção plena, tendo colocado de lado a cobiça e o
desprazer pelo mundo.[</span><a href="#N7">7</span></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>]<o:p></o:p></span></p>

<p class=Normal><a name=R7></a><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>( Contemplação
do Corpo )</b><o:p></o:p></span></p>

<p class=Normal><i><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>( 1. Atenção Plena na
Respiração )</b><o:p></o:p></span></i></p>

<p class=Normal><a name=T4></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. &quot; E
como, bhikkhus, um bhikkhu permanece contemplando o corpo como um corpo? Aqui
um bhikkhu, dirigindo-se à floresta ou à sombra de uma árvore ou a um local
isolado; senta-se com as pernas cruzadas, mantém o corpo ereto e estabelecendo
a plena atenção à sua frente, [<a href="#N7a">7a</a>] <a name=R7a></a>ele
inspira com atenção plena justa, ele expira com atenção plena justa. [<a
href="#N7b">7b</a>] <a name=Ra></a><a name=R7b></a>Inspirando longo, ele
compreende : ‘Eu inspiro longo’; ou expirando longo, ele compreende: ‘Eu expiro
longo.’ Inspirando curto, ele compreende: ‘Eu inspiro curto’; ou expirando
curto, ele compreende: ‘Eu expiro curto.’ [</span><a href="#N8">8</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R8></a>Ele treina dessa
forma: ‘Eu inspiro experienciando todo o corpo [da respiração]’; ele treina
dessa forma: ‘Eu expiro experienciando todo o corpo [da respiração].’ [</span><a
href="#N9">9</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R9></a>Ele treina dessa
forma: ‘Eu inspiro tranqüilizando a formação do corpo [da respiração]’: ele
treina dessa forma: ‘ Eu expiro tranqüilizando a formação do corpo [da
respiração].’[</span><a href="#N10">10</span></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>] <a name=R10></a>Da mesma forma como um torneiro habilidoso ou seu
aprendiz, quando faz uma volta longa, compreende: ‘Eu faço uma volta longa’;
ou, quando faz uma volta curta, compreende: ‘Eu faço uma volta curta’; da mesma
forma, inspirando longo, um Bhikkhu compreende: ‘Eu inspiro longo’... ele
treina dessa forma: ‘Eu devo expirar tranqüilizando a formação do corpo.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(Insight)</b><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. &quot; Dessa forma ele
permanece contemplando o corpo como um corpo internamente, ou ele permanece
contemplando o corpo como um corpo externamente, ou ele permanece contemplando
o corpo como um corpo tanto interna como externamente. [</span><a href="#N11">11</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R11></a>Ou então, ele
permanece contemplando fenômenos que surgem no corpo, ou ele permanece
contemplando fenômenos que desaparecem no corpo, ou ele permanece contemplando
ambos, fenômenos que surgem e fenômenos que desaparecem no corpo. [</span><a
href="#N12">12</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R12></a>Ou então, a
atenção plena de que ‘existe um corpo’ se estabelece somente na medida
necessária para o conhecimento e para a continuidade da atenção plena. [</span><a
href="#N13">13</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R13></a>E ele permanece
independente, sem nenhum apego a qualquer coisa mundana. Assim é como um
bhikkhu permanece contemplando o corpo como um corpo.<o:p></o:p></span></p>

<p class=Normal><i><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>( 2. As Quatro Posturas
)</b><o:p></o:p></span></i></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. &quot; Novamente,
bhikkhus, quando caminhando, um bhikkhu compreende: ‘Eu estou caminhando’;
quando em pé, ele compreende: ‘Eu estou em pé’; quando sentado, ele compreende:
‘Eu estou sentado’; quando deitado, ele compreende: ‘Eu estou deitado’; ou ele
compreende a postura do corpo conforme for o caso. [</span><a href="#N14">14</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. &quot; <a name=R14></a>Dessa
forma ele permanece contemplando o corpo como um corpo internamente,
externamente, tanto interna como externamente ... E ele permanece independente,
sem nenhum apego a qualquer coisa mundana. Assim também é como um bhikkhu 
permanece contemplando o corpo como um corpo.<o:p></o:p></span></p>

<p class=Normal><i><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>( 3. Plena Consciência )</b><o:p></o:p></span></i></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. &quot; Novamente,
bhikkhus, um bhikkhu age com plena consciência ao ir para a frente e retornar; [</span><a
href="#N15">15</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R15></a>age com plena
consciência ao olhar para frente e desviar o olhar; age com plena consciência
ao dobrar e estender os membros; age com plena consciência ao carregar o manto
externo, o manto superior, a tigela; age com plena consciência ao comer, beber,
mastigar e saborear; age com plena consciência ao urinar e defecar; age com
plena consciência ao caminhar, ficar em pé, sentar, dormir, acordar, falar e
permanecer em silêncio.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. &quot; Dessa forma ele
 permanece contemplando o corpo como um corpo internamente, externamente,
tanto interna como externamente ... E ele permanece independente, sem
nenhum apego a qualquer coisa mundana. Assim também é como um bhikkhu
permanece contemplando o corpo como um corpo.<o:p></o:p></span></p>

<p class=Normal><i><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>( 4. Repulsa – As
Partes do Corpo )</b><o:p></o:p></span></i></p>

<p class=Normal><a name=T10></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. &quot;
Novamente, bhikkhus, um bhikkhu examina esse mesmo corpo para cima, a partir da
sola dos pés e para baixo, a partir do topo da cabeça, limitado pela pele e
repleto de muitos tipos de coisas repulsivas, portanto: ‘Neste corpo existem cabelos,
pêlos do corpo, unhas, dentes, pele, carne, tendões, ossos, tutano, rins,
coração, fígado, diafragma, baço, pulmões, intestino grosso, intestino delgado,
conteúdo do estômago, fezes, bílis, fleuma, pus, sangue, suor, gordura,
lágrimas, saliva, muco, líquido sinovial e urina.’ [</span><a href="#N16">16</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R16></a>Como se houvesse
um saco com uma abertura em uma extremidade cheio de vários tipos de grãos,
como arroz sequilho, arroz vermelho, feijões, ervilhas, milhete e arroz branco,
e um homem com vista boa o abrisse e examinasse: ‘Isto é arroz sequilho, arroz
vermelho, feijões, ervilhas, milhete e arroz branco’; da mesma forma, um
bhikkhu examina esse mesmo corpo ... repleto de muitos tipos de coisas repulsivas: ‘ Neste
corpo existem cabelos ... e urina.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>11. &quot; Dessa maneira,
ele permanece contemplando o corpo como um corpo internamente, externamente,
tanto interna como externamente ... E ele permanece independente, sem
nenhum apego a qualquer coisa mundana. Assim também é como um bhikkhu
permanece contemplando o corpo como um corpo.<o:p></o:p></span></p>

<p class=Normal><i><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>( 5. Elementos )</b><o:p></o:p></span></i></p>

<p class=Normal><a name=T12></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. &quot;
Novamente, bhikkhus, um bhikkhu examina esse mesmo corpo que,<span
style='mso-spacerun:yes'>  </span>não importando a sua posição ou postura,
consiste de elementos da seguinte forma: ‘Neste corpo há o elemento terra, o
elemento água, o elemento fogo, e o elemento ar.’ [</span><a href="#N17">17</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R17></a>Do mesmo modo,
como se um açougueiro habilidoso ou seu aprendiz tivesse matado uma vaca e
estivesse sentado numa encruzilhada com a vaca em pedaços; assim também um
bhikkhu examina esse mesmo corpo que ... consiste de elementos, portanto:
‘Neste corpo há o elemento terra, o elemento água, o elemento fogo e o elemento
ar.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. &quot; Dessa forma
ele permanece contemplando o corpo como um corpo internamente, externamente,
tanto interna como externamente ... E ele permanece independente, sem nenhum
apego a qualquer coisa mundana. Assim também é como um bhikkhu  permanece
contemplando o corpo como um corpo.<o:p></o:p></span></p>

<p class=Normal><i><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>( 6. As Nove
Contemplações do Cemitério )</b><o:p></o:p></span></i></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>14. &quot; Novamente,
bhikkhus, como se ele visse um cadáver jogado em um cemitério, [<a href="#N17a">17a</a>]
<a name=R17a></a>um, dois, ou três dias depois de morto, inchado, lívido e
esvaindo matéria, um bhikkhu compara seu corpo com ele: ‘Este corpo também tem
a mesma natureza, se tornará igual, não está isento desse destino.’ [</span><a
href="#N18">18</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. &quot; <a name=R18></a>Dessa
forma ele permanece contemplando o corpo como um corpo internamente,
externamente, tanto interna como externamente ... E ele permanece independente,
sem nenhum apego a qualquer coisa mundana. Assim também é como um bhikkhu
permanece contemplando o corpo como um corpo.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16. &quot; Novamente, como
se ele visse um cadáver jogado num cemitério, sendo devorado por corvos,
gaviões, abutres, cães, chacais ou vários tipos de vermes, um bhikkhu compara
seu mesmo corpo com ele: ‘Este corpo também tem a mesma natureza, se tornará
igual, não está isento desse destino.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17. &quot;...Assim também é
como um bhikkhu permanece contemplando o corpo como um corpo.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18-24.&quot; Novamente,
bhikkhus, como se ele visse um cadáver jogado num cemitério, um esqueleto com
carne e sangue, que se mantém unido por tendões ... um esqueleto descarnado
lambuzado de sangue, que se mantém unido por tendões...um esqueleto descarnado
e sem sangue, que se mantém unido por tendões...ossos desconectados espalhados
em todas as direções – aqui um osso da mão, ali um osso do pé, aqui um osso da
perna, ali um osso da coxa, aqui um osso da bacia, ali um osso da coluna
vertebral, aqui uma costela, ali um osso do peito, aqui um osso do braço, ali
um osso do ombro, aqui um osso do pescoço, ali um osso da mandíbula, aqui um
dente, ali um crânio - um Bhikkhu compara seu corpo com ele, portanto: ‘Este
corpo também tem a mesma natureza, se tornará igual, não está isento desse
destino.’ [</span><a href="#N19">19</span></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>]<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>25. &quot;<a name=R19></a>...Assim
também é como um bhikkhu permanece contemplando o corpo como um corpo.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>26-30.&quot; Novamente,
bhikkhus, como se ele visse um cadáver jogado num cemitério, os ossos brancos
desbotados, a cor de conchas...ossos amontoados, com mais de um ano ... ossos
apodrecidos e esfarelados convertidos em pó, um bhikkhu compara seu corpo com
ele, portanto: ‘ Este corpo também tem a mesma natureza, se tornará igual, não
está isento desse destino.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(Insight)</b><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>31. &quot;Dessa forma ele permanece contemplando 
o corpo como um corpo internamente, ou ele permanece contemplando o corpo como um corpo externamente, ou ele permanece 
contemplando o corpo como um corpo tanto interna como externamente. Ou então, ele permanece contemplando fenômenos 
que surgem no corpo, ou ele permanece contemplando fenômenos que desaparecem no corpo, ou ele permanece contemplando ambos, 
fenômenos que surgem e fenômenos que desaparecem no corpo. Ou então, a atenção plena ‘de que existe um corpo’ se estabelece 
somente na medida necessária para o conhecimento e para a continuidade da atenção plena. E ele permanece independente, 
sem nenhum apego a qualquer coisa mundana. Assim é como um Bhikkhu permanece contemplando o corpo no corpo.<o:p></o:p></span></p>

<p class=Normal><a name=T32></a><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(
Contemplação das Sensações )</b><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>32. &quot; E como,
bhikkhus, um bhikkhu permanece contemplando sensações como sensações? [</span><a
href="#N20">20</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R20></a>Aqui, sentindo uma
sensação prazerosa, um bhikkhu compreende: ‘Eu sinto uma sensação prazerosa’ [<a
href="#N20a">20a</a>]; <a name=R20a></a>quando sente uma sensação dolorosa, ele
compreende: ‘Eu sinto uma sensação dolorosa’ [ <a href="#N20b">20b</a>]; <a
name=R20b></a>quando sente uma sensação nem prazerosa, nem dolorosa, ele
compreende: ‘Eu sinto uma sensação nem prazerosa, nem dolorosa’. [<a href="#N20c">20c</a>]
<a name=R20c></a>Quando sente uma sensação prazerosa mundana, ele compreende:
‘Eu sinto uma sensação prazerosa mundana’; quando sente uma sensação prazerosa
não mundana, ele compreende: ‘Eu sinto uma sensação prazerosa não mundana’;
quando sente uma sensação dolorosa mundana, ele compreende: ‘Eu sinto uma
sensação dolorosa mundana’; quando sente uma sensação dolorosa não mundana, ele
compreende: ‘Eu sinto uma sensação dolorosa não mundana’; quando sente uma
sensação nem prazerosa, nem dolorosa mundana, ele compreende: ‘Eu sinto uma
sensação nem prazerosa, nem dolorosa mundana’; quando sente uma sensação nem
prazerosa, nem dolorosa não mundana, ele compreende: ‘Eu sinto uma sensação nem
prazerosa, nem dolorosa não mundana’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(Insight)</b><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>33. &quot; Dessa forma ele
permanece contemplando as sensações como sensações internamente ou ele
permanece contemplando as sensações como sensações externamente, [<a href="#N20d">20d</a>]
<a name=R20d></a>ou ele permanece contemplando as sensações<sub> </sub>como
sensações tanto interna como externamente. Ou então, ele permanece contemplando
fenômenos que surgem nas sensações, ou ele permanece contemplando fenômenos que
desaparecem nas sensações, ou ele permanece contemplando ambos fenômenos que
surgem e fenômenos que desaparecem nas sensações. [</span><a href="#N21">21</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R21></a>Ou então, a
atenção plena ‘de que existem sensações’ se estabelece somente na medida
necessária para o conhecimento e para a continuidade da atenção plena. E ele
permanece independente, sem nenhum apego a qualquer coisa mundana. Assim é como
um bhikkhu permanece contemplando sensações como sensações.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>( Contemplação da Mente )</b><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>34. &quot; E como,
bhikkhus, um bhikkhu permanece contemplando a mente como mente? [</span><a
href="#N22">22</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R22></a>Aqui um bhikkhu
compreende a mente afetada pelo desejo como mente afetada pelo desejo e a mente
não afetada pelo desejo como mente não afetada pelo desejo. Ele compreende a
mente afetada pela raiva como mente afetada pela raiva e a mente não afetada
pela raiva como mente não afetada pela raiva. Ele compreende a mente afetada
pela delusão como mente afetada pela delusão e a mente não afetada pela delusão
como mente não afetada pela delusão. Ele compreende a mente contraída <a
name=R22a></a>como mente contraída e a mente distraída como
mente distraída. Ele compreende a mente transcendente como
mente transcendente e a mente não transcendente como mente não transcendente.
Ele compreende a mente superável como mente superável e a mente não superável
como mente não superável. Ele compreende a mente concentrada como mente
concentrada e a mente não concentrada como mente não concentrada. Ele
compreende a mente libertada como mente libertada e a mente não libertada como
mente não libertada. [</span><a href="#N23">23</span></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>]<o:p></o:p></span></p>

<p class=Normal><a name=R23></a><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>(Insight)</b><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>35. &quot; Dessa forma ele
permanece contemplando a mente como mente internamente ou ele permanece
contemplando a mente como mente externamente, ou ele permanece contemplando a
mente como mente tanto interna como externamente. Ou então, ele permanece
contemplando fenômenos que surgem na mente, ou ele permanece contemplando
fenômenos que desaparecem na mente, ou ele permanece contemplando ambos, os
fenômenos que surgem como os fenômenos que desaparecem na mente.[</span><a
href="#N24">24</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=R24></a>Ou então, a
atenção plena ‘de que existe a mente’ se estabelece somente na medida
necessária para o conhecimento e para a continuidade da atenção plena. E ele
