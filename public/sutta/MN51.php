<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Assim ouvi.
Em certa ocasião o Abençoado estava em Campa às margens do lago Gaggara com uma
grande Sangha de bhikkhus. Então Pessa, o filho do treinador de elefantes e
Kandaraka o errante foram até o Abençoado. Pessa, depois de homenagear o
Abençoado, sentou a um lado, enquanto que Kandaraka cumprimentou o Abençoado e
quando a troca de saudações corteses e amigáveis havia terminado, ele ficou em
pé a um lado. [<a href="#N1">1</a>] <a name=R1></a>Estando ali em pé, ele
inspecionou a Sangha de bhikkhus sentados em completo silêncio, [<a href="#N2">2</a>]
<a name=R2></a>e então disse para o Abençoado:<o:p></o:p></span></p>



<p class=Normal><a name=T2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2. “É maravilhoso, Mestre Gotama, é admirável como a Sangha de bhikkhus
tem sido conduzida a praticar da forma correta pelo Mestre Gotama. Aqueles que
foram Abençoados, arahants, perfeitamente iluminados no passado, no máximo,
conduziram a Sangha de bhikkhus apenas a praticar da forma correta como é feito
agora pelo Mestre Gotama. E aqueles que serão Abençoados, arahants, perfeitamente
iluminados no futuro, no máximo, conduzirão a Sangha de bhikkhus apenas a
praticar da forma correta como é feito agora pelo Mestre Gotama.” [<a href="#N3">3</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>3. “Assim é, Kandaraka, assim é! Aqueles que foram Abençoados, arahants,
perfeitamente iluminados no passado, no máximo, conduziram a Sangha de bhikkhus
apenas a praticar da forma correta como é feito agora por mim. E aqueles que
serão Abençoados, arahants, perfeitamente iluminados no futuro, no máximo,
conduzirão a Sangha de bhikkhus apenas a praticar da forma correta como é feito
agora por mim. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Kandaraka,
nesta Sangha de bhikkhus existem bhikkhus que são arahants, livres das
impurezas e que têm vivido a vida santa, fizeram o que deve ser feito,
depuseram o fardo, alcançaram o verdadeiro objetivo, destruíram os grilhões da
existência e estão completamente libertados através do conhecimento supremo.
Nesta Sangha de bhikkhus existem bhikkhus no treinamento superior, com a
virtude constante, vivendo uma vida de virtude constante, com sabedoria, vivendo
uma vida com sabedoria constante. Eles permanecem com as mentes bem
estabelecidas nos quatro fundamentos da atenção plena. [<a href="#N4">4</a>] <a
name=R4></a>Quais quatro? Nesse caso, Kandaraka, um bhikkhu permanece
contemplando o corpo como um corpo, ardente, plenamente consciente e com
atenção plena, tendo colocado de lado a cobiça e o desprazer pelo mundo. Ele
permanece contemplando as sensações como sensações, ardente, plenamente
consciente e com atenção plena, tendo colocado de lado a cobiça e o desprazer pelo
mundo. Ele permanece contemplando a mente como mente, ardente, plenamente
consciente e com atenção plena, tendo colocado de lado a cobiça e o desprazer
pelo mundo. Ele permanece contemplando os objetos mentais como objetos mentais,
ardente, plenamente consciente e com atenção plena, tendo colocado de lado a
cobiça e o desprazer pelo mundo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. Quando
isso foi dito, Pessa, o filho do treinador de elefantes, disse: “É maravilhoso,
venerável senhor, é admirável quão bem os quatro fundamentos da atenção plena
foram expostos pelo Abençoado: para a purificação dos seres, para a superação
da tristeza e lamentação, para o desaparecimento da dor e da angústia, para
realizar o caminho verdadeiro, para realizar Nibbana. De tempos em tempos,
venerável senhor, nós pessoas leigas vestidas de branco também permanecemos com
as nossas mentes bem estabelecidas nesses quatro fundamentos da atenção plena.
[<a href="#N5">5</a>] <a name=R5></a>Nesse caso, venerável senhor, permanecemos
contemplando o corpo como um corpo ... sensações como sensações ... mente como
mente ... objetos mentais como objetos mentais, ardentes, plenamente conscientes
e com atenção plena, tendo colocado de lado a cobiça e o desprazer pelo mundo.
É maravilhoso, venerável senhor, é admirável como no meio do emaranhamento,
corrupção e engano dos homens, o Abençoado compreenda aquilo que é benéfico e
prejudicial para os seres. Pois a humanidade é emaranhada mas, um animal é mais
simples. Venerável senhor, eu sou capaz de conduzir um elefante domável, e no
tempo que irá tomar para fazer uma viagem de ida e volta até Campa, o elefante
irá tentar todo tipo truque, duplicidade, desonestidade e logro (da qual ele é
capaz). [<a href="#N6">6</a>] <a name=R6></a>Mas, aqueles que são os nossos
escravos, mensageiros e serviçais se comportam de uma forma com o corpo, uma
outra forma com a linguagem, enquanto que a mente deles opera ainda de uma
outra forma. É maravilhoso, venerável senhor, é admirável como no meio do
emaranhamento, corrupção e engano dos homens, o Abençoado compreenda aquilo que
é benéfico e prejudicial para os seres. Pois a humanidade é emaranhada mas, um
animal é mais simples.”<o:p></o:p></span></p>



<p class=Normal><a name=T5></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>5. “Assim é, Pessa, assim é! A humanidade é emaranhada mas, um animal é
mais simples. Pessa, existem quatro tipos de pessoas que podem ser encontradas
no mundo. [<a href="#N7">7</a>] <a name=R7></a>Quais quatro? É o caso de um
tipo de pessoa que atormenta a si mesma e se dedica à prática de torturar a si
mesma. É o caso de um tipo de pessoa que atormenta os outros e se dedica à
prática de torturar os outros. É o caso de um tipo de pessoa que atormenta a si
mesma e se dedica à prática de torturar a si mesma e ela também atormenta os
outros e se dedica à prática de torturar os outros. É o caso de um tipo de
pessoa que não atormenta a si mesma nem se dedica à prática de torturar a si
mesma e ela também não atormenta os outros nem se dedica à prática de torturar
os outros. Visto que ela não atormenta a si mesma nem aos outros, ela está aqui
e agora sem fome, saciada, arrefecida, permanece experimentando a bem-aventurança,
tendo ela mesma se tornado santa.[<a href="#N8">8</a>] <a name=R8></a>Qual
desses quatro tipos de pessoas satisfaz a sua mente, Pessa?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Os três
primeiros não satisfazem a minha mente, venerável senhor, mas o último satisfaz
a minha mente.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. “Mas,
Pessa, porque os três primeiros tipos de pessoas não satisfazem a sua mente?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, o tipo de pessoa que atormenta a si mesma e se dedica à prática de
torturar a si mesma, atormenta e tortura a si mesma embora deseje o prazer e
abomine a dor; é por isso que esse tipo de pessoa não satisfaz a minha mente. E
o tipo de pessoa que atormenta os outros e se dedica à prática de torturar os
outros, atormenta e tortura os outros que desejam o prazer e abominam a dor; é
por isso que esse tipo de pessoa não satisfaz a minha mente. E o tipo de pessoa
que atormenta a si mesma e se dedica à prática de torturar a si mesma e ela
também atormenta os outros e se dedica à prática de torturar os outros, sendo
que ambos desejam o prazer e abominam a dor; é por isso que esse tipo de pessoa
não satisfaz a minha mente. Mas o tipo de pessoa que não atormenta a si mesma
nem se dedica à prática de torturar a si mesma e ela também não atormenta os
outros, nem se dedica à prática de torturar os outros; esta pessoa, visto que
não atormenta a si mesma nem aos outros, está aqui e agora sem fome, saciada,
arrefecida, permanece experimentando a bem-aventurança, tendo ela mesma se
tornado santa – ela não atormenta nem tortura nem a si mesma, nem aos outros
pois, ambos desejam o prazer e abominam a dor. É por isso que esse tipo de
pessoa satisfaz a minha mente. E agora, venerável senhor, nós partiremos.
Estamos ocupados e temos muito o que fazer.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Agora é o
momento, Pessa, faça como julgar adequado.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então
Pessa, o filho do treinador de elefantes, tendo ficado satisfeito e contente
com as palavras do Abençoado, levantou-se do seu assento<span
style='mso-spacerun:yes'>  </span>e depois de homenagear o Abençoado,
mantendo-o à sua direita, partiu.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. Pouco
tempo depois dele haver partido, o Abençoado se dirigiu aos bhikkhus da
seguinte forma: “Bhikkhus, Pessa, o filho do treinador de elefantes, é sábio,
possui muita sabedoria. Se ele permanecesse sentado um pouco mais, enquanto eu
expusesse em detalhe esses quatro tipos de pessoas, ele teria se beneficiado
muito. Mas, mesmo assim, ele já obteve um grande benefício.” [<a href="#N9">9</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R9></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Agora é o momento, Abençoado, agora é o momento, Iluminado, para que o
Abençoado ensine em detalhe esses quatro tipos de pessoas. Tendo ouvido do
Abençoado os bhikkhus o recordarão.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
bhikkhus, ouçam e prestem muita atenção àquilo que eu vou dizer.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
venerável senhor,” os bhikkhus responderam. O Abençoado disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><a name=T8></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>8. “Bhikkhus, que tipo de pessoa atormenta a si mesma e se dedica à prática
de torturar a si mesma? [<a href="#N10">10</a>] <a name=R10></a>Neste caso uma
certa pessoa anda nua, rejeitando as convenções, lambendo as mãos, não
atendendo quando chamada, não parando quando solicitada; não aceita que tragam
comida ou comida feita especialmente para ela, ou convite para comer; ela não
aceita comida diretamente de um pote ou de uma panela na qual foi cozida, ou
comida colocada numa soleira, ou colocada onde está a lenha, ou colocada onde
estão os pilões, ou de duas pessoas comendo juntas, ou de uma mulher grávida,
ou de uma mulher amamentado, ou de uma mulher num grupo com homens, ou
de um lugar que tenha divulgado a distribuição de comida, onde um cachorro
esteja esperando, onde moscas estejam zunindo; ela não aceita peixe ou carne,
ela não aceita bebidas alcoólicas, vinho, ou mingau de arroz fermentado. Ela se
restringe a uma casa, a um bocado; ela se restringe a duas casas, a dois
bocados…ela se restringe a sete casas, a sete bocados. Ela vive com um pires de
comida por dia, dois pires de comida por dia …. sete pires de comida por dia;
Ela come uma vez por dia, uma vez cada dois dias … uma vez cada sete dias, e
assim por diante até uma vez cada quinze dias. Ou ela come ervas, ou capim, ou
arroz selvagem, ou plantas aquáticas, ou farelo de arroz, ou escuma de arroz
cozido, ou flores de plantas oleaginosas, ou estrume de vaca, ou<span
style='mso-spacerun:yes'>  </span>raízes e frutas da floresta, ou frutas
caídas. Ela se veste com cânhamo, com mortalhas, com trapos, com casca de árvores,
com pele de antílopes, com tiras de pele de antílopes, com capim, com cabelos
humanos, com pelos do rabo de cavalos, com penas das asas de corujas. Ela
arranca cabelos e barba, dedicando-se à prática de arrancar os cabelos e a
barba. Ela fica em pé continuamente, rejeitando assentos. Ela fica de cócoras
continuamente, devotada a manter a posição de cócoras. Ela usa um colchão com
espinhos; ela faz de um colchão com espinhos a sua cama. Ela dorme no chão. Ela
dorme sempre do mesmo lado. O seu corpo está coberto com imundície. Ela vive e dorme
ao ar livre. Ela se alimenta com imundície, dedicando-se à prática de comer os
quatro tipos de imundície (esterco de vaca, urina de vaca, cinzas e argila).
Ela nunca bebe água fria. Ela purifica o corpo com três imersões na água a cada
dia. A isto se chama o tipo de pessoa que atormenta a si mesma e se dedica à
prática de torturar a si mesma.<o:p></o:p></span></p>



<p class=Normal><a name=T9></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>9. “ Que tipo de pessoa atormenta os outros e se dedica à prática de
torturar os outros? Neste caso uma certa pessoa é um açougueiro de ovelhas, um
açougueiro de porcos, uma caçadora de aves, arma armadilhas para capturar
animais selvagens, uma caçadora, pescadora, ladra, carrasco, guarda de prisão,
ou alguém que se dedique a qualquer uma dessas ocupações sanguinárias. A isto
se chama o tipo de pessoa que atormenta os outros e se dedica à prática de
torturar os outros.<o:p></o:p></span></p>



<p class=Normal><a name=T10></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>10. “Que tipo de pessoa atormenta a si mesma e se dedica à prática de
torturar a si mesma e ela também atormenta os outros e se dedica à prática de
torturar os outros? Neste caso uma pessoa é um rei ungido ou um brâmane
próspero. [<a href="#N11">11</a>] <a name=R11></a>Tendo construído um novo
templo para sacrifícios no lado leste da cidade, e raspando o cabelo e a barba,
vestindo-se com peles grosseiras de animais, tendo untado o corpo com manteiga
líquida e óleo, arranhado as costas com um chifre de um gamo, ele entra no
templo junto com a rainha e o alto sacerdote brâmane. Lá ele se deita sobre o
chão coberto com capim. O rei se alimenta do leite da primeira mama de uma vaca
com um bezerro da mesma cor enquanto que a rainha se alimenta do leite da
segunda mama e o alto sacerdote brâmane se alimenta do leite da terceira mama;
o leite da quarta mama eles derramam sobre o fogo e o bezerro sobrevive com o
leite que restar. Ele diz o seguinte: ‘Que tantos touros sejam abatidos como
sacrifício, que tantos bois sejam abatidos como sacrifício, que tantos novilhos
sejam abatidos como sacrifício, que tantos bodes sejam abatidos como
sacrifício, que tantos carneiros sejam abatidos como sacrifício, que tantas
árvores sejam cortadas para postes de sacrifício, que tanto capim seja cortado
para o capim de sacrifício.’ E então os seus escravos, mensageiros e serviçais
realizam as suas tarefas com os rostos cobertos de lágrimas, incitados pelas
ameaças de punição e pelo medo. A isto se chama o tipo de pessoa que atormenta
a si mesma e se dedica à prática de torturar a si mesma e ela também atormenta
os outros e se dedica à prática de torturar os outros.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-spacerun:yes'> </span>11. “Que tipo de pessoa, bhikkhus, não
atormenta a si mesma nem se dedica à prática de torturar a si mesma e ela
também não atormenta os outros, nem se dedica à prática de torturar os outros –
aquela que, visto que ela não atormenta a si mesma nem aos outros, está aqui e
agora sem fome, saciada, arrefecida, permanece experimentando a
bem-aventurança, tendo ela mesma se tornado santa?[<a href="#N12">12</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R12></a><a name=T12></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>12. “Neste caso, bhikkhus, um Tathagata surge
no mundo, um arahant, perfeitamente iluminado, consumado no verdadeiro
conhecimento e conduta, bem-aventurado, conhecedor dos mundos, um líder
insuperável de pessoas preparadas para serem treinadas, mestre de devas e
humanos, desperto, sublime. Ele declara - tendo realizado por si próprio com o
conhecimento direto - este mundo com os seus devas, maras e brahmas, esta
população com seus contemplativos e brâmanes, seus príncipes e o povo. Ele
ensina o Dhamma, com o significado e fraseado corretos, que é admirável no
início, admirável no meio, admirável no final; e ele revela uma vida santa que
é completamente perfeita e imaculada. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>13. &quot;Um chefe de
família ou o filho de um chefe de família ou alguém nascido em algum outro clã
ouve o Dhamma. Ouvindo o Dhamma ele adquire convicção<span
style='mso-spacerun:yes'>  </span>no Tathagata. Possuindo essa fé ele reflete
da seguinte forma: 'A vida em família é confinada, um caminho empoeirado; a
vida santa é como o ar livre. Não é fácil viver em casa e praticar a vida santa
completamente perfeita, totalmente pura, como uma concha polida. E se eu
raspasse o meu cabelo e barba, vestisse os mantos de cor ocre e seguisse a vida
santa.' Então após algum tempo ele abandona a sua fortuna, grande ou pequena;
deixa o seu círculo de parentes, grande ou pequeno; raspa o seu cabelo e barba,
veste o manto de cor ocre e segue a vida santa.<o:p></o:p></span></p>

<p class=Normal><a name=T14></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>14. “Tendo
seguido a vida santa e de posse do treinamento e estilo de vida de um bhikkhu,
abandonando tirar a vida de outros seres, ele se abstém de tirar a vida de
outro seres; ele permanece com a sua vara e arma postas de lado, bondoso e
gentil, compassivo com todos os seres vivos. Abandonando tomar o que não seja
dado, ele se abstém de tomar o que não é dado; tomando somente aquilo que é
dado, aceitando somente aquilo que é dado, não roubando ele permanece puro.
Abandonando o não celibato, ele vive uma vida celibatária, vive separado,
abstendo-se da prática vulgar do ato sexual.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Abandonando a linguagem
mentirosa, ele se abstém da linguagem mentirosa; ele fala a verdade, mantém a
verdade, é firme e confiável, não é um enganador do mundo. Abandonando a
linguagem maliciosa, ele se abstém da linguagem maliciosa; o que ouviu aqui ele
não conta ali para separar aquelas pessoas destas, nem o que ele ouviu lá não
conta aqui para separar estas pessoas daquelas; assim ele reconcilia aquelas
pessoas que estão divididas, promove a amizade, ele ama a concórdia, se delicia
com a concórdia, desfruta da concórdia, diz coisas que criam a concórdia.
Abandonando a linguagem grosseira, ele se abstém da linguagem grosseira. Ele
diz palavras que são gentis, que agradam aos ouvidos, carinhosas, que penetram
o coração, que são corteses, desejadas por muitos e que agradam a muitos.
Abandonando a linguagem frívola, ele se abstém da linguagem frívola. Ele fala
na hora certa, diz o que é fato, aquilo que é bom, fala de acordo com o Dhamma
e a Disciplina; nas horas adequadas ele diz palavras que são úteis, racionais,
moderadas e que trazem benefício. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele se abstém de danificar
sementes e plantas. Ele come somente uma vez ao dia, privando-se da refeição
noturna e de alimentos nas horas incorretas. Ele se abstém de dançar, cantar,
ouvir música e de ver espetáculos de entretenimento. Ele se abstém de usar
ornamentos, usar perfumes e de embelezar o corpo com cosméticos. Ele se abstém
de deitar em leitos elevados e luxuosos. Ele se abstém de aceitar ouro e
dinheiro. Ele se abstém de aceitar grãos que não estejam cozidos. Ele se abstém
de aceitar carne crua. Ele se abstém de aceitar mulheres e garotas. Ele se
abstém de aceitar escravos homens e mulheres. Ele se abstém de aceitar cabras e
ovelhas. Ele se abstém de aceitar aves e porcos. Ele se abstém de aceitar
elefantes, gado, cavalos e éguas. Ele se abstém de aceitar terras e
propriedades. Ele se abstém de fazer pequenas tarefas e levar mensagens. Ele se
abstém de comprar e vender. Ele se abstém de lidar com balanças falsas, metais falsos,
falsas medidas. Ele se abstém do suborno, burla e fraude. Ele se abstém de
mutilar, executar, aprisionar, roubar, pilhar e violentar.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>15. “Ele está satisfeito
com os mantos que protegem o seu corpo e com os alimentos esmolados que mantêm
o seu estômago, e aonde quer que vá ele apenas leva essas coisas consigo. Igual
a um passarinho, aonde quer que ele vá, voa com as asas como seu único fardo,
assim também, o bhikkhu está satisfeito com os mantos que protegem o seu corpo
e com os alimentos esmolados que mantêm o seu estômago, e aonde quer que vá ele
apenas leva essas coisas consigo. Possuindo esse agregado da nobre virtude, ele
experimenta dentro de si uma felicidade que é imaculada.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>16. “Ao ver uma forma com o
olho, ele não se agarra aos seus sinais ou detalhes. Visto que, se 
permanecer com a faculdade do olho descuidada, ele será tomado pelos estados ruins e 
prejudiciais de cobiça e tristeza, ele pratica a contenção, ele protege a
faculdade do olho, ele se empenha na contenção da faculdade do olho. Ao ouvir
um som com o ouvido ... Ao cheirar um aroma com o nariz … Ao saborear um sabor
com a língua … Ao tocar algo tangível com o corpo … Ao conscientizar um
objeto mental<span style='mso-spacerun:yes'>  </span>com a<span
style='mso-spacerun:yes'>  </span>mente, ele não se agarra aos seus sinais ou
detalhes. Visto que, se permanecer com a faculdade da mente descuidada,
ele será tomado pelos estados ruins e 
prejudiciais de cobiça e tristeza, ele
pratica a contenção, ele protege a faculdade da mente, ele se empenha na
contenção da faculdade da mente. Dotado dessa nobre contenção das faculdades,
ele experimenta dentro de si uma felicidade que é imaculada.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>17. “Ele age com plena
consciência ao ir para a frente e retornar; <a name=R15></a>age com plena
consciência ao olhar para frente e desviar o olhar; age com plena consciência
ao dobrar e estender os membros; age com plena consciência ao carregar o manto
externo, o manto superior, a tigela; age com plena consciência ao comer, beber,
mastigar e saborear; age com plena consciência ao urinar e defecar; age com
plena consciência ao caminhar, ficar em pé, sentar, dormir, acordar, falar e
permanecer em silêncio.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>18. &quot;Dotado desse
nobre agregado da virtude, essa nobre contenção das faculdades sensoriais, essa
nobre atenção plena e plena consciência, ele procura<span
style='mso-spacerun:yes'>  </span>um local isolado: na floresta, à sombra de
uma árvore, uma montanha, uma ravina, uma caverna em uma encosta, um cemitério,
um matagal, um espaço aberto, uma cabana vazia.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>19. Depois de esmolar
alimentos, após a refeição, ele senta com as pernas cruzadas, com o corpo ereto
colocando a atenção plena à sua frente. Abandonando a cobiça pelo mundo, ele
permanece com a mente livre de cobiça; ele purifica a sua mente da cobiça.
Abandonando a má vontade, ele permanece com a mente livre de má
vontade, com compaixão pelo bem estar de todos os seres vivos; ele purifica a
sua mente da má vontade. Abandonando a preguiça e o torpor, ele
permanece livre da preguiça e do torpor, perceptivo à luz, atento e plenamente
consciente; ele purifica sua mente da preguiça e do torpor. Abandonando a inquietação
e a ansiedade, ele permanece calmo com a mente em paz; ele purifica sua mente
da inquietação e da ansiedade. Abandonando a dúvida, ele assim permanece tendo
superado a dúvida, sem perplexidade em relação a qualidades mentais hábeis; ele
purifica a mente da dúvida. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>20. “Tendo assim abandonado
esses cinco obstáculos, imperfeições da mente que enfraquecem a sabedoria,
um bhikkhu afastado dos prazeres sensuais, afastado das qualidades não hábeis, entra e permanece no primeiro jhana, que é caracterizado pelo pensamento aplicado e sustentado, com o êxtase e felicidade nascidos do afastamento.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>21. “Além disso,
abandonando o pensamento aplicado e sustentado, um bhikkhu entra e permanece no segundo jhana, que é caracterizado pela segurança interna e perfeita unicidade da mente, sem o pensamento aplicado e sustentado, com o êxtase e felicidade nascidos da concentração. Ele permeia e impregna, cobre e preenche esse corpo com o êxtase
e o prazer nascidos da concentração.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>22. “Além disso, abandonando o êxtase, um bhikkhu entra e permanece no terceiro jhana que é caracterizado pela felicidade sem o êxtase, acompanhada pela atenção plena, plena consciência e equanimidade, acerca do qual os nobres declaram: ‘Ele permanece numa estada feliz, equânime e plenamente atento.’<span
style='mso-spacerun:yes'>  </span><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>23. “Além disso, com o completo desaparecimento da felicidade, um bhikkhu entra e permanece no quarto jhana, que possui nem felicidade nem sofrimento, com a atenção plena e a equanimidade purificadas.<o:p></o:p></span></p>

<p class=Normal><a name=T24></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>24. &quot;Com
a sua mente dessa forma concentrada, purificada, luminosa, pura, imaculada,
livre de defeitos, flexível, maleável, estável e<span
style='mso-spacerun:yes'>  </span>atingindo a imperturbabilidade, ele a dirige
para o conhecimento da recordação de vidas passadas. Ele se recorda das suas
muitas vidas passadas, isto é, um nascimento, dois nascimentos, três
nascimentos, quatro, cinco, dez, vinte, trinta, quarenta, cinqüenta, cem, mil,
cem mil, muitos ciclos cósmicos de contração, muitos ciclos cósmicos de
expansão, muitos ciclos cósmicos de contração e expansão, ‘Lá eu tive tal nome,
pertencia a tal clã, tinha tal aparência. Assim era o meu alimento, assim era a
minha experiência de prazer e dor, assim foi o fim da minha vida. Falecendo
desse estado, eu renasci ali. Ali eu também tinha tal nome, pertencia a tal
clã, tinha tal aparência. Assim era o meu alimento, assim era a minha
experiência de prazer e dor, assim foi o fim da minha vida. Falecendo daquele
estado, eu renasci aqui.’ Assim ele se recorda das suas muitas vidas passadas
nos seus modos e detalhes.<o:p></o:p></span></p>

<p class=Normal><a name=T25></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>25. &quot;Com a sua mente dessa forma concentrada, purificada, luminosa,
pura, imaculada, livre de defeitos, flexível, maleável, estável e atingindo a
imperturbabilidade, ele a dirige para o conhecimento do falecimento e
reaparecimento dos seres. Por meio do olho divino, que é purificado e ultrapassa
o humano, ele vê seres falecendo e renascendo, inferiores e superiores, bonitos
e feios, afortunados e desafortunados. Ele compreende como os seres prosseguem
de acordo com as suas ações desta forma: ‘Esses seres – dotados de má conduta
com o corpo, linguagem e mente, que insultam os nobres, com o entendimento
incorreto e realizando ações sob a influência do entendimento incorreto – com a
dissolução do corpo, após a morte, renasceram num estado de privação, num
destino infeliz, nos reinos inferiores, até mesmo no <span style='mso-bidi-font-weight:
bold'>inferno</span>. Porém estes seres - dotados de boa conduta com o corpo,
linguagem e mente, que não insultam os nobres, com o<span
style='mso-spacerun:yes'>  </span>entendimento correto e realizando ações sob a
influência do entendimento correto – com a dissolução do corpo, após a morte,
renasceram num destino feliz, no paraíso.’ Dessa forma - por meio do olho
divino, que é purificado e ultrapassa o humano<span style='mso-spacerun:yes'> 
</span>- ele vê seres falecendo e renascendo, inferiores e superiores, bonitos
e feios, e ele compreende como os seres prosseguem de acordo com as suas ações.<o:p></o:p></span></p>

<p class=Normal><a name=T26></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>26.
&quot;Com a sua mente dessa forma concentrada, purificada, luminosa, pura,
imaculada, livre de defeitos, flexível, maleável, estável e atingindo a
imperturbabilidade, ele a dirige para o conhecimento do fim das impurezas
mentais. Ele compreende como na verdade é que: ‘Isto é sofrimento’; ele
compreende como na verdade é que: ‘Esta é a origem do sofrimento’; ele
compreende como na verdade é que: ‘Esta é a cessação do sofrimento’; ele
compreende como na verdade é que: ‘Este é o caminho que conduz à cessação do
sofrimento’; ele compreende como na verdade é que: ‘Essas são impurezas
mentais’; ele compreende como na verdade é que: ‘Esta é a origem das
impurezas’; ele compreende como na verdade é que: ‘Esta é a cessação das
impurezas’; ele compreende como na verdade é que: ‘Este é o caminho que conduz
à cessação das impurezas.’ <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>27. “Ao conhecer e ver, a
sua mente está livre da impureza do desejo sensual, da impureza de ser/existir,
da impureza da ignorância. Quando ela está libertada surge o conhecimento,
‘Libertada.’ Ele compreende que ‘O nascimento foi destruído, a vida santa foi
vivida, o que deveria ser feito foi feito, não há mais vir a ser a nenhum
estado.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>28. “A isto, bhikkhus, se chama
o tipo de pessoa que não atormenta a si mesma nem se dedica à prática de
torturar a si mesma e ela também não atormenta os outros nem se dedica à
prática de torturar os outros -<span style='mso-spacerun:yes'>  </span>aquela
que, visto que não atormenta a si mesma nem aos outros, está aqui e agora sem
fome, saciada, arrefecida, permanece experimentando a bem-aventurança, tendo
ela mesma se tornado santa.”<o:p></o:p></span></p>

<p class=Normal><a name=R31></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Isso foi o que disse o Abençoado. Os bhikkhus ficaram satisfeitos e
contentes com as palavras do Abençoado. <o:p></o:p></span></p>

