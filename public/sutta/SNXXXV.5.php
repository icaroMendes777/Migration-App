<p class=Normal>Assim ouvi. Em certa ocasião o Abençoado estava em Savatthi no Bosque de Jeta, no Parque de Anathapindika. Lá ele se dirigiu aos monges desta forma: “Bhikkhus” – “Venerável Senhor,” eles responderam. O Abençoado disse o seguinte:</p>

<p class=Normal> “Bhikkhus, as formas são sofrimento. Aquilo que é sofrimento é não-eu. Aquilo que é não-eu deve ser visto com clareza como na verdade é, com correta sabedoria, assim: 'Isso não é meu, isso não sou eu, isso não é o meu eu.'</p>

<p class=Normal> "Os sons são sofrimento... os aromas são sofrimento... os sabores são sofrimento... os tangíveis são sofrimento... os objetos mentais são sofrimento. Aquilo que é sofrimento é não-eu. Aquilo que é não-eu deve ser visto com clareza como na verdade é, com correta sabedoria, assim: 'Isso não é meu, isso não sou eu, isso não é o meu eu.'</p>

<p class=Normal> "Vendo assim, bhikkhus, o nobre discípulo bem instruído experimenta o desencantamento em relação às formas, o desencantamento em relação aos aromas, o desencantamento em relação aos sabores, o desencantamento em relação aos tangíveis, o desencantamento em relação aos objetos mentais. Experimentando o desencantamento ele se torna desapegado. Através do desapego a sua  mente está libertada. Quando está libertada, surge o conhecimento, ‘Libertada.’ Ele compreende que ‘O nascimento foi destruído, a vida santa foi vivida, o que devia ser feito foi feito, não há mais vir a ser a nenhum estado.'"</p>

