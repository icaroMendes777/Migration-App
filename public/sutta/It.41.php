<p class=Normal> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: "Bhikkhus, aqueles seres desprovidos de sabedoria são completamente destituídos. Eles permanecem no desconforto aqui e agora, aflitos, perturbados, angustiados, com a dissolução do corpo após a morte, um destino ruim pode ser esperado."</p>

<p class=Normal> "Aqueles seres não desprovidos de sabedoria não são completamente destituídos. Eles permanecem com conforto aqui e agora, sem aflição, sem perturbação, sem angústia, com a dissolução do corpo após a morte, um destino bom pode ser esperado."</p>

<p class=Normal style='margin-left:.75in'> Olhem para este mundo com os seus devas,<br>
desprovido de sabedoria,<br>
estabelecido na mentalidade-materialidade (nome e forma),<br>
concebendo ser essa a verdade.</p>

<p class=Normal style='margin-left:.75in'> A sabedoria que conduz à realização<br>
é a melhor coisa no mundo;<br>
pois assim alguém pode ser capaz de compreender completamente <br>
o fim de ambos nascimento e ser/existir.</p>

<p class=Normal style='margin-left:.75in'>Devas e humanos estimam<br>
os despertos dotados de atenção plena,<br>
possuidores de alegre sabedoria,<br>
suportando o último corpo.</p>


