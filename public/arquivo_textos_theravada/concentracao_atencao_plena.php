<p class=Normal>Muitas pessoas nos dizem que o Buda ensinou dois tipos diferentes de
meditação - meditação da atenção plena e meditação da concentração. A meditação
da atenção plena, elas dizem, é o caminho direto, enquanto que a prática da
concentração equivale à rota panorâmica que você toma por sua própria conta e
risco, pois é muito fácil se deixar envolver e nunca mais conseguir escapar.
Mas quando você investiga aquilo que o Buda realmente ensinou, ele nunca
separou essas duas práticas. Ambas são partes de um todo. Todas as vezes em que
ele explica a atenção plena e o seu papel no caminho, ele esclarece que o
propósito da atenção plena é conduzir a mente ao estado de Concentração Correta
- fazer com que a mente se acalme e encoontre o lugar onde possa sentir-se
realmente equilibrada, em casa, de onde possa olhar para as coisas com
constância e vê-las pelo que na verdade são.</p>

<p class=Normal>Parte do problema das &quot;duas práticas&quot; se deve à forma como
entendemos a palavra <i>jhana,</i> que é um sinônimo para Concentração Correta.
Muitos de nós ouvimos que jhana é um estado bastante intenso, semelhante a um
transe, que requer uma contemplação intensa e o bloqueio do resto do mundo. Não
soa nada parecido com a atenção plena. Mas se você procurar no Cânone a
descrição do Buda para jhana, esse não é o estado ao qual ele se refere. Estar
em jhana é estar absorvido, de forma muito agradável, na sensação de todo o
corpo, completamente. Uma sensação de consciência ampla preenche todo o corpo.
Uma das imagens usadas pelo Buda para descrever esse estado é aquela de uma
pessoa misturando água com pó de banho, de tal forma que a água permeie o pó de
banho por toda a parte. Outra, é um lago em que uma fonte fresca surge do fundo
e se espalha por todo o lago. </p>

<p class=Normal>Agora, quando você está com o corpo como um todo, você se encontra
intensamente no momento presente. Você está exatamente ali todo o tempo. Como o
Buda disse, o quarto jhana - em que o corpo está repleto com a consciência
luminosa - é o ponto em que a atenção plena e a equanimidade se tornam puras.
Portanto, não deveria haver nenhum problema em combinar a prática da atenção
plena com a consciência de todo o corpo que se torna bastante calma e
tranqüila. Na verdade, o próprio Buda as combina na descrição dos quatro
primeiros passos da meditação da respiração: (1) estar consciente da respiração
longa, (2) estar consciente da respiração curta, (3) estar consciente de todo o
corpo da respiração à medida em que você inspira e expira, (4) acalmar a
respiração. Isto, os textos nos dizem, é a prática básica da atenção plena.
Também é uma prática básica da concentração. Com esses passos você estará
entrando no primeiro jhana - Concentração Correta e ao mesmo tempo estará
praticando a Atenção Plena Correta. </p>

<p class=Normal>Para ver como a Atenção Plena Correta e a Concentração Correta se auxiliam
mutuamente na prática, podemos verificar os três estágios da prática da atenção
plena expostos no <a href="../sutta/MN10.php">Sutta dos Fundamentos da Atenção
Plena</a>. Tome o corpo como um exemplo. O primeiro estágio é manter o foco no
corpo, colocando de lado a cobiça e o desprazer pelo mundo. Isto significa
tomar o corpo como um corpo, sem pensar nele em termos do que ele significa ou
do que ele pode fazer no mundo. Ele pode ser bonito ou feio. Pode ser forte ou
fraco. Pode ser ágil ou desajeitado - todas as questões que nos preocupam
quando pensamos sobre nós mesmos. O Buda disse para colocar essas questões de
lado. </p>

<p class=Normal>Simplesmente, estar com o corpo, sentado exatamente aqui. Você fecha os
olhos - o que você sente? Existe a sensação corpórea resultante do fato de<span
style="mso-spacerun: yes">  </span>você estar sentado. Esse é o seu fundamento.
Tente permanecer com ela. Insista em trazer a mente de volta para essa sensação
corpórea até que ela capte a mensagem e comece a se acalmar. No início da
prática você irá ver a mente escapar para se agarrar a isto ou aquilo, você
observa apenas o suficiente para dizer-lhe que se solte daquilo, retorne ao
corpo e permaneça ali. Então ela escapa para se agarrar a alguma outra coisa,
você lhe diz para se soltar, retornar e mais uma vez se prender ao corpo.<span
style="mso-spacerun: yes">  </span>Até que, finalmente, você chega a um ponto
em que consegue se fixar na respiração e você não irá soltá-la, de acordo? Você
a mantém fixa. A partir desse ponto, qualquer outra coisa que venha a entrar em
contato com a sua consciência será como algo surgindo e roçando as costas da
sua mão. Você não precisa notar isso. Você permanece com o corpo como o seu
fundamento básico. As outras coisas surgem e desaparecem, você tem consciência
delas, mas você não abandona a respiração para se agarrar a elas. Nesse ponto
você realmente estabeleceu o corpo como um fundamento sólido. </p>

<p class=Normal>Ao fazer isso, você desenvolveu três qualidades na mente. Uma é a atenção
plena <i>(sati). </i>O termo atenção plena quer dizer ser capaz de lembrar,
manter algo presente na mente. No caso de utilizar o corpo como um fundamento,
significa ser capaz de lembrar onde você deve permanecer - com o corpo - sem
permitir que você se esqueça. A segunda qualidade, plena consciência <i>(sampajañña),</i>
significa estar consciente daquilo que está acontecendo no presente. Você está
com o corpo? Você está com a respiração? A respiração está confortável?
Simplesmente, note o que está acontecendo no momento presente. Nós tendemos a
confundir atenção plena com plena consciência, mas na verdade elas são coisas
distintas: a atenção plena significa ser capaz de lembrar onde você quer manter
a sua consciência; plena consciência significa estar consciente daquilo que
está realmente acontecendo. A terceira qualidade, ardência <i>(atappa),</i>
significa duas coisas. Um, se você se der conta que a mente está vagando, você
a traz de volta. Imediatamente. Você não permite que ela fique vagando, cheirando
as flores. Dois, quando a mente está com o seu fundamento, ardência significa tentar, tanto quanto possível, ser sensível àquilo que
está acontecendo - não sendo apenas levado pelo momento presente, mas tentando
realmente penetrar mais e mais os detalhes sutis daquilo que realmente está
ocorrendo com a respiração ou a mente. </p>

<p class=Normal>Quando você possui essas três qualidades focadas no corpo, você não poderá
deixar de acalmar-se e sentir-se realmente confortável com o corpo no momento
presente. É quando você está preparado para o segundo estágio da prática, que é
descrita como ter a consciência da origem e da cessação. Nesse estágio você
estará tentando entender causa e efeito tal como eles ocorrem no presente. Com
relação à prática da concentração, uma vez que você tenha acalmado a mente,
você deve entender a interação entre causa e efeito no processo de
concentração, de forma que você possa fazer com que ela se estabeleça de
maneira mais sólida por períodos de tempo mais longos, em todos os tipos de
situação, na almofada de meditação e fora dela. Para fazer isso, você precisa aprender
como as coisas surgem e cessam na mente, não somente pela observação, mas pelo
real envolvimento com o seu surgimento e cessação. </p>

<p class=Normal>Você pode observar isso nas instruções do Buda de como lidar com os
obstáculos. No primeiro estágio, ele diz para ter consciência dos obstáculos
conforme eles surgem e cessam. Algumas pessoas pensam que se trata de um
exercício de &quot;consciência imparcial&quot;, no qual você tenta não forçar a
mente para qualquer direção, você simplesmente senta e observa, desejando ou
não, tudo aquilo que surge na mente. Em termos práticos, no entanto, a mente
ainda não está preparada para isso. O que você precisa neste estágio é de um
ponto de referência fixo para avaliar os eventos na mente, da mesma forma como
quando você está tentando avaliar o movimento das nuvens no céu: você precisa
escolher um ponto fixo - tal como uma cumeeira de um telhado ou um poste de luz
- para o qual possa fixar o olhar de forrma a ter uma noção da direção e
velocidade das nuvens. O mesmo com relação ao surgimento e cessação dos desejos
sensuais, má vontade, etc., na mente:<span style="mso-spacerun: yes"> 
</span>Você precisa tentar manter um ponto de referência fixo para a mente -
como a respiração - se você realmente quiser ter sensibilidade para identificar
quando os obstáculos na mente estiverem interferindo com o seu ponto de
referência ou não. </p>

<p class=Normal>Suponha que a raiva esteja interferindo com a sua concentração. Ao invés de
se envolver com a raiva, você tenta simplesmente estar consciente de quando ela
está presente e quando não. Você olha para a raiva como um evento em si e por
si mesmo - como ela surge, como ela desaparece. Mas você não para por aí. O
próximo passo - enquanto você ainda está empenhado em focar na respiração - é o
de reconhecer como fazer com que a a raiva desapareça. Algumas vezes, apenas o
ato de observá-la é o suficiente para fazer com que ela despareça, outras vezes
não e você terá então que lidar com ela de outras formas, tal como debatendo as
razões por trás da raiva ou lembrando a si mesmo das desvantagens da raiva.
Como parte do processo de lidar com ela, você terá que &quot;por a mão na
massa&quot;. Você terá que tentar entender porque a raiva está surgindo, porque
ela está desaparecendo, como você pode fazer com que ela saia dali, pois você
se dá conta de que ela representa um estado inábil. E isso exige uma certa dose
de improvisação. Experimentação. Você tem que tirar o ego e a impaciência da
frente para poder ter o espaço para cometer erros e aprender com eles, para que
você possa desenvolver a habilidade de lidar com a raiva. Não se trata apenas
de odiar a raiva e tentar colocá-la de lado, ou de amá-la e recebê-la de braços
abertos. Essas abordagens podem trazer resultados no curto prazo, mas no longo
prazo elas não são particularmente hábeis. O requisito neste caso é a
habilidade para ver a composição da raiva e como você pode desfazê-la. </p>

<p class=Normal>Uma técnica que gosto de usar - quando a raiva está presente e estou numa
situação em que não preciso reagir imediatamente - é simplesmente perguntar a
mim mesmo de maneira bem humorada, &quot;Muito bem, porque você está com
raiva?&quot; Ouça aquilo que a mente tem a dizer. Então prossiga com o tema:
&quot;Mas porque você está com raiva <i>disso?</i>&quot; &quot;Claro que estou
com raiva. Pois&quot; &quot;Bem, mas porque você está com raiva disso?&quot;
Se você prosseguir dessa forma, a mente irá eventualmente admitir algo tolo,
como por exemplo a suposição de que as pessoas não deveriam se comportar assim
- apesar de ser óbvio que elas <i>são </i>assim - ou que as pessoas deveriam
agir de acordo com os seus padrões, ou qualquer outra coisa pela qual a mente
se sente tão envergonhada que tenta esconder de você. Mas no final, se você
continuar sondando, ela irá se revelar. Desta forma, você obterá um profundo
entendimento da raiva e isso poderá enfraquecer o poder que ela tem sobre você.
</p>

<p class=Normal>Com relação às qualidades positivas como a atenção plena, tranqüilidade e
concentração, ocorre algo semelhante. Primeiro, você tem a consciência de
quando elas estão presentes e quando não estão para então se dar conta de que é
muito mais agradável quando estão presentes do que quando não estão. Assim,
você tenta compreender <i>como </i>elas surgem e como cessam. Você faz isso
tentando manter conscientemente o estado de atenção plena e concentração. Se
você for realmente observador - e essa é a essência de tudo, ser observador -
você começará a perceber que existem formas hábeis de manter esse estado sem
estar preocupado em falhar ou ser bem sucedido, sem permitir que o desejo de
obter um estado mental tranqüilo interfira com o processo de tranqüilizar a mente.
Você quer ser bem sucedido, mas precisa ter uma atitude equilibrada em relação
ao fracasso e ao êxito de forma a aprender com eles. Ninguém está registrando
num marcador ou avaliando notas. Você está aqui para aprender em seu próprio
benefício. Dessa forma, esse processo de desenvolver o seu fundamento da
atenção plena não é &quot;simplesmente observar&quot;. É muito mais a
participação no processo de surgimento e cessação - na verdade brincar com o
processo - de maneira que você possa aprender com a experiência de como o
fenômeno de causa e efeito atuam na mente. </p>

<p class=Normal>Certa vez, quando eu estava na universidade, escrevi para casa reclamando da
comida e a minha mãe me mandou um livro de receitas da Julia Child. No livro
havia uma seção sobre como lidar com ovos, onde ela dizia que um <a name=PVW>sinalizador</a>
de um bom cozinheiro era o seu conhecimento a respeito de ovos. Assim, eu
peguei um ovo. Você pode observar um ovo - você pode aprender certas coisas
apenas ao observá-lo, mas você não aprende muito. Para aprender sobre ovos você
tem que colocá-los em uma frigideira e tentar fritá-los. Se você fizer isso
durante algum tempo irá começar a compreender que os ovos possuem variações
e<span style="mso-spacerun: yes">  </span>que eles se comportam de certas
formas em relação ao calor e de outras em relação ao óleo, manteiga ou o que
quer que seja. E assim, ao trabalhar com o ovo e tentar fazer algo com ele,
você passa de verdade a entender de ovos. Ocorre algo semelhante com a argila:
você realmente não entende de argila até que se torne um oleiro e tente realizar
algo com a argila. </p>

<p class=Normal>E o mesmo ocorre com a mente: a não ser que você realmente tente fazer
alguma coisa com a sua mente, tente estabelecer um estado mental e mantê-lo,
você não conhecerá a sua mente verdadeiramente. Você não conhecerá os processos
de causa e efeito dentro da mente. Tem que existir um elemento de verdadeira
participação no processo. Dessa maneira,<span style="mso-spacerun: yes"> 
</span>você poderá entendê-la. Tudo isso se resume em ser observador e
desenvolver uma habilidade. A essência do desenvolvimento de uma habilidade
significa duas coisas. Um, você tem consciência de uma situação tal como ela se
apresenta, e dois, você tem consciência do seu envolvimento nela. Quando o Buda
fala sobre causas, ele diz que todas as situações são moldadas por dois lados -
as causas que vêm do passado e as causas que você está colocando no presente.
Você tem que estar sensível a ambas. Se você não tiver sensibilidade em relação
àquilo que você está colocando em uma certa situação, você nunca irá
desenvolver nenhum tipo de habilidade. Como você tem consciência daquilo que
está fazendo, você também observa os resultados. Se algo não estiver certo,
você retorna e modifica o que fez - seguindo dessa forma até que obtenha os
resultados que você deseja. E durante o processo você aprende sobre a argila,
os ovos, ou qualquer outra coisa que você esteja tentando lidar com habilidade.
</p>

<p class=Normal>O mesmo se aplica à mente. É claro, você poderia aprender algo sobre a mente
ao tentar alcançar qualquer tipo de estado, mas para o propósito de desenvolver
o insight realmente penetrante, um estado de concentração estável, equilibrado
e plenamente atento é o melhor tipo de suflê ou pote de cerâmica no qual
você irá querer estabelecer a sua mente. Os fatores de prazer, conforto e ,
algumas vezes, até mesmo êxtase que surgem quando a mente realmente se acalma
ajudam você a permanecer com conforto no momento presente, com um centro de
gravidade baixo. Uma vez que a mente esteja firmemente estabelecida dessa
forma, você terá algo para investigar por um bom período de tempo, de maneira
que possa ver qual é a sua composição. No típico estado mental desequilibrado,
as coisas aparecem e desaparecem muito rapidamente para que você possa notá-las
com clareza. Mas como o Buda notou, quando você desenvolve a habilidade com jhana,
você pode retroceder um pouco e ver com clareza como as coisas são. Você pode
ver, digamos, onde existe um elemento de apego, onde existe um elemento de
estresse, ou mesmo onde existe inconstância dentro do seu estado equilibrado. É
nesse ponto que você começa a obter o insight, quando você vê as linhas
naturais de segmentação entre os diferentes fatores da mente e, em particular,
a linha de segmentação entre a consciência e os objetos da consciência. </p>

<p class=Normal>Uma outra vantagem desse estado plenamente atento e concentrado é que você
irá se sentir cada vez mais à vontade nele, você começará a se dar conta de que
é possível ter felicidade e prazer na vida sem ter que depender de coisas
externas - pessoas, relacionamentos, aprovação de outros ou qualquer outra coisa
proveniente de ser parte do mundo. Essa compreensão ajuda a libertá-lo dos seus
apegos às coisas externas. Algumas pessoas temem se apegar a um estado de
tranqüilidade mas, na verdade, é muito importante que você se apegue nesse
caso, para que você comece a se acalmar e a se desfazer dos seus apegos
externos. Só quando esse apego à calma for o único restante, você irá começar a
trabalhar para afrouxá-lo também. </p>

<p class=Normal>Ainda uma outra razão porque a concentração sólida é necessária para o
insight é que quando o discernimento surge na mente, a lição básica que ele lhe
ensinará é que você tem sido estúpido. Você se apegou a coisas mesmo sabendo,
lá no fundo, que não valia a pena. Agora, tente convencer as pessoas disso
quando elas estão famintas e cansadas. Elas reagirão de imediato, &quot;Você
também é estúpido&quot;, e esse será o fim da discussão. Não se chegará a lugar
nenhum. Mas se você conversar com alguém que esteja bem alimentado e
descansado, você pode levantar todos os tipos de assuntos sem correr o risco de
uma briga. É o mesmo com a mente. Quando ela estiver bem alimentada com o
êxtase e a tranqüilidade da concentração, ela estará pronta para aprender. Ela
aceitará as suas críticas sem se sentir ameaçada ou maltratada. </p>

<p class=Normal>Então, esse é o papel que a concentração desempenha neste segundo estágio da
prática da atenção plena. Ela lhe oferece algo com que brincar, uma habilidade
a ser desenvolvida, para que você possa começar a entender os fatores de causa
e efeito dentro da mente. Você começa a ver a mente apenas como um fluxo de
causas com os seus efeitos retornando até você. As suas idéias são parte desse
fluxo de causa e efeito, as suas emoções, a sua noção de quem você é. Esse
insight começa a afrouxar os seus apegos a todo esse processo. </p>

<p class=Normal>O que acontece, finalmente, é que no terceiro nível na prática da atenção
plena a mente alcança um estado de equilíbrio perfeito  onde você desenvolveu
um tal estado de concentração e de equilíbrio ao ponto de não ter que adicionar
nada mais. No sutta dos Fundamentos da Atenção Plena isso é descrito,
simplesmente, como estar consciente - se você estiver usando o corpo como
fundamento- é estar consciente de que &quot;Existe um corpo&quot;, o suficiente
para o conhecimento e a atenção plena, sem estar apegado a nada no mundo.
Outros textos denominam esse estado de &quot;não formação&quot;. A mente
alcança um ponto em que você começa a compreender que todos os processos
causais na mente - incluindo os processos de concentração e insight - são como
bonecas de piche. Se você gostar delas, você ficará grudado; se você não
gostar, você ficará grudado também. Então, o que fazer? Você tem que chegar ao
ponto em que na realidade não estará contribuindo com nada mais para o momento
presente. Você desata a sua participação dele. É quando as coisas se abrem na
mente. </p>

<p class=Normal>Muitas pessoas querem dar um salto e começar direto por esse
nível, o de não adicionar nada ao momento presente, mas não é assim que
funciona. Você não consegue ter sensibilidade para as coisas sutis que a mente
está habitualmente adicionando ao presente, a não ser que você tente
conscientemente alterar aquilo que você está adicionando. À medida que a sua
habilidade for melhorando, você terá mais sensibilidade para as coisas sutis
que você fazia, mas não se dava conta. Você, então, alcança um ponto de desencantamento
e é quando compreende que a maneira mais hábil para lidar com o presente é
eliminar todos os níveis de participação, até mesmo aqueles que causam a mínima
parcela de estresse na mente. Você começa a desmontar os níveis de participação
que aprendeu no segundo estágio da prática, até o ponto em que as coisas
alcançam o equilíbrio por si mesmas, onde existe o soltar-se de tudo e o
libertar-se.</p>

<p class=Normal>Portanto, é importante compreender que existem esses três estágios na
prática da atenção plena, e de entender o papel que a prática deliberada da
concentração desempenha em conduzi-lo através dos dois primeiros. Sem ter como
objetivo a Concentração Correta, você não poderá desenvolver as habilidades
necessárias para compreender a mente - pois é durante o processo de conquista
da habilidade da concentração plenamente atenta que o verdadeiro insight surge.
Da mesma forma como você não entende realmente o que é um rebanho de gado, até
que você o tenha arrebanhado com êxito - aprendendo com todos os seus fracassos
durante o processo - você não será capaz de obter a compreensão de todas as
correntes de causa e efeito que percorrem a mente, até que você tenha aprendido
com os seus fracassos e êxitos a fazer com que ela se mantenha num estado de
concentração plenamente atenta e atenção plena concentrada. E só quando você
tiver realmente entendido e dominado essas correntes - as do desejo que causam
sofrimento e estresse e as da atenção plena e da concentração que formam o
Caminho - você poderá soltar-se e libertar-se delas. </p>


