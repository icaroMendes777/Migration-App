<p class=Tit1 align=center style='text-align:center'>Leigh Brasington</Tit1>

<p class=Normal>Leigh Brasington tem sido praticante Budista desde 1985 e ele é o discípulo sênior norte-americano da falecida mestra <a href="ayya_khema.php"> Ayya Khema </a> tendo sido autorizado por ela a ensinar os jhanas. Ele nasceu e cresceu no Mississippi, filho de um pastor Presbiteriano. Leigh perdeu a fé na religião de sua família com 18 anos de idade ao ler o livro ‘The Source’ de James Michener, um romance épico que descreve as origens do Judaísmo. “Em essência, Deus morreu naquele verão,” uma década mais tarde ele “despertou para a dimensão espiritual da vida” enquanto viajava pela Ásia.<o:p></o:p></span></p>

<p class=Normal>A prática de fato, no entanto, teve início apenas muitos anos mais tarde, quando ele participou de um retiro de meditação liderado por Ayya Khema, que por fim acabou ajudando Leigh a encontrar o caminho através dos jhanas e o encorajou a ensinar.<o:p></o:p></span></p>

<p class=Normal>Na atualidade ele passa muitos meses no ano viajando pelos Estados Unidos, Europa 
