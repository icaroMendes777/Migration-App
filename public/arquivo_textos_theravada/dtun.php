<p class=Tit1 align=center style='text-align:center'>Ajaan Dtun Thiracitto</Tit1>


<p class=Normal>Ajaan Dtun (Thiracitto) nasceu na província de Ayutthaya, na Tailândia, em 1955. Com seis anos de idade a sua família mudou-se para Bangkok onde morou até 1978.</p>

<p class=Normal>Desde pequeno ele era um menino cujo coração naturalmente se inclinava pela disciplina moral. Durante a  adolescência e nos anos em que cursou a universidade, houve muitos pequenos incidentes que moldaram a sua vida e gradualmente o afastaram dos caminhos do mundo na direção da vida santa.</p.>

<p class=Normal>Depois de se formar em 1978 como Bacharel em Economia, ele foi aceito em um curso de Mestrado em Urbanismo na Universidade de Colorado, nos EUA. No entanto, no período em que ele estava se preparando para viajar para o exterior muitos pequenos insights se amalgamaram com força e assim, ao invés de desejar conduzir os seus estudos tão a fundo quanto pudesse para em seguida ter uma vida familiar, ele mudou a sua maneira de pensar, para depois de se formar permanecer solteiro e trabalhar com o objetivo de ajudar financeiramente o seu pai até que surgisse o momento apropriado para se ordenar como monge. Uma noite, aconteceu dele folhear um livro do Dhamma que pertencia ao seu pai e por acaso ele abriu na página com as últimas palavras do Buda: "Dessa forma, bhikkhus, eu os encorajo: todas as coisas condicionadas estão sujeitas à dissolução. Esforcem-se pelo objetivo com diligência." Lendo pela segunda e depois pela terceira vez, as palavras ressoaram profundamente no seu coração, fazendo com que ele sentisse que o tempo para se ordenar havia chegado, sabendo que essa era a única coisa que lhe traria algum benefício verdadeiro. Ele decidiu com firmeza que dentro de dois meses ele iria se ordenar como monge e que a sua ordenação seria pelo resto da vida.</p>

<p class=Normal>Em Junho de 1978 ele viajou para a província de Ubon Ratchathani para ser ordenado por Ajaan Chah em Wat Nong Pah Pong. Decidido por natureza e determinado em sua prática ele alcançou progresso constante, independentemente de estar morando com Ajaan Chah ou afastado em qualquer um dos monastérios afiliados ao Wat Nong Pah Pong. Em 1981, ele retornou à região central da Tailândia para passar o retiro das chuvas em Wat Fah Krahm (perto de Bangkok), juntamente com Ajaan Piak e Ajaan Anan. Os três permaneceram vivendo e praticando juntos em Wat Fah Krahm até o final de 1984. Então Ajaan Anan e Ajaan Dtun foram convidados a fixar residência em um pequeno pedaço de floresta na província de Rayong, no leste da Tailândia. Vendo que o local era inadequado para residência a longo prazo, Ajaan Dtun escolheu outro local que foi disponibilizado - uma montanha coberta com floresta que mais tarde se tornaria o atual Wat Marp Jan.</p>

<p class=Normal>Depois de passar cinco anos auxiliando Ajaan Anan no estabelecimento de Wat Marp Jan, ele decidiu que era hora de procurar um período de isolamento de modo a intensificar a sua prática, sabendo que isso seria necessário se ele quisesse finalmente concluir a prática do Dhamma. Ele foi convidado a se instalar numa área de oitenta hectares de floresta densa, na província de Chonburi, permanecendo em relativo isolamento durante dois anos até 1992, quando finalmente decidiu aceitar a oferta dessas terras para o estabelecimento de um monastério - Wat Boonyawad. Atualmente, o monastério se espalha por mais de 160 hectares.</p>

<p class=Normal>Desde que em 1993 autorizou que monges viessem morar no monastério, Ajaan Dtun desenvolveu uma reputação crescente como um professor de destaque dentro da tradição das florestas da Tailândia, atraindo entre quarenta a cinqüenta monges para viverem e praticarem sob a sua orientação.</p>

