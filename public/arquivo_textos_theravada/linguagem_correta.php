<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Meu mestre dizia, &quot;Se
você não consegue controlar a sua boca, não existe esperança de que irá
controlar a sua mente.&quot; Por essa razão a linguagem correta é tão
importante na prática diária. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A Linguagem Correta
explicada em termos negativos significa evitar quatro tipos de linguagem que
são prejudiciais: mentiras (palavras que são ditas com a intenção de distorcer
a verdade); maldosas (palavras ditas com a intenção de separar as pessoas);
grosseiras (palavras ditas com a intenção de ferir outra pessoa
emocionalmente); e frívolas (palavras ditas sem a intenção de algum
significado). <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Observe o foco na intenção:
é nesse ponto que a prática da linguagem correta cruza com o treinamento da
mente. Antes de você falar, você foca no <i>porquê </i>você quer falar. Isso
ajuda você a tomar contato com todas as maquinações que estão ocorrendo no
comitê de vozes que dirigem a sua mente. Se você percebe alguma motivação
inábil se ocultando por detrás das decisões do comitê, você poderá vetá-las.
Como resultado você estará mais atento consigo mesmo, mais honesto consigo
mesmo, mais firme consigo mesmo. Você também evita dizer coisas das quais se
arrependerá mais tarde. Dessa forma você fortalece qualidades da mente que lhe
auxiliarão na meditação, ao mesmo tempo que evita qualquer tipo de recordação
dolorosa que atrapalharia a atenção no momento presente, quando chegar a
ocasião de meditar. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em termos positivos, a
linguagem correta significa falar de forma honesta, harmoniosa, confortante e
digna de ser levada a sério. Quando você pratica essas formas positivas de
linguagem correta as suas palavras se convertem em bênçãos para os outros. Em
resposta, as outras pessoas irão prestar mais atenção àquilo que você tem a
dizer e provavelmente irão responder da mesma forma. Isso lhe dá uma idéia do
poder das suas ações: a maneira como você age no momento presente <i>de fato </i>molda
o mundo das suas experiências. Você não precisa ser uma vítima de eventos
passados. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Para muitos de nós a parte
mais difícil da prática da linguagem correta se encontra na forma como
expressamos nosso senso de humor. Especialmente quando damos boas gargalhadas
através do exagero, sarcasmo, estereótipos e pura bobagem - todos exemplos
clássicos de linguagem incorreta. Se as pessoas se acostumam com esse tipo de
humor negligente, elas irão parar de ouvir com cuidado aquilo que temos a
dizer. Dessa forma vulgarizamos o nosso discurso. Na verdade já existe ironia
suficiente no mundo portanto,<span style="mso-spacerun: yes">  </span>não
precisamos exagerar ou ser sarcásticos. Os melhores humoristas são aqueles que
simplesmente nos fazem ver diretamente as coisas como elas são. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Expressar o nosso humor de
forma honesta, útil e sábia pode exigir que pensemos e nos esforcemos, mas
quando nos tornamos mestres nesse tipo de sutileza nos damos conta de que o
esforço valeu a pena. Aguçamos a nossa mente e melhoramos a nossa linguagem.
Dessa forma, mesmo as nossas piadas se tornam parte da nossa prática: uma
oportunidade para desenvolver qualidades positivas na mente e oferecer algo de
valor intelectual para as pessoas que nos rodeiam. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim, tenha muito cuidado
