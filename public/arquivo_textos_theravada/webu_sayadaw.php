<p class=Tit1 align=center style='text-align:center'>Venerável Webu Sayadaw</Tit1>

<p class=Normal>O Venerável Webu Sayadaw, nasceu no dia 17 de Fevereiro de 1896 em Ingyinbin, um pequeno vilarejo próximo a Shwebo no norte da Birmânia (atual Myanmar). Ele foi ordenado como noviço aos nove anos de idade tendo recebido o nome Shin Kumara. Com vinte anos ele foi ordenado como bhikkhu recebendo o nome de U Kumara. (Webu Sayadaw é um título que significa “o santo mestre de Webu” que lhe foi dado depois que ele se estabeleceu como mestre).<o:p></o:p></span></p>

<p class=Normal>U Kumara foi para Mandalay para estudar no famoso Monastério Masoyein, a mais destacada universidade na época. No sétimo ano após a sua ordenação ele abandonou os estudos das escrituras em Pali e deixou o monastério para por em prática aquilo que ele havia aprendido sobre meditação.<o:p></o:p></span></p>

<p class=Normal>Depois de deixar o monastério, U Kumara passou quatro anos solitário. Daí, ele foi para o seu vilarejo natal Ingyinbin para uma breve visita onde, a pedido do seu antigo mestre do monastério, ensinou-lhe a técnica de meditação que ele havia adotado. Ele disse: “Este é um atalho para Nibbana. Qualquer um pode usá-lo. Pode ser  investigado e está de acordo com os ensinamentos do Buda conforme as escrituras. É o caminho direto para Nibbana.”<o:p></o:p></span></p>
<p class=Normal>Webu Sayadaw enfatizava a prática da meditação como o único caminho para realizar os ensinamentos do Buda. O estudo das escrituras, embora ajude, não é essencial para a realização de Nibbana. A técnica de meditação ensinada por Webu Sayadaw é anapana sati, “atenção plena na respiração,” que exige que a pessoa esteja consciente e atenta à inspiração ao inspirar, à expiração ao expirar e com atenção no ponto ou área na qual o fluxo de ar toca enquanto entra ou sai pelas narinas. Embora anapana sati seja usado como método para desenvolver samadhi, a concentração da mente, Webu Sayadaw dizia que quando a concentração é desenvolvida o suficiente, o meditador pode obter o insight das três características dos fenômenos – impermanência, insatisfatório e não-eu. O entendimento direto dessas três características é chamado pañña, sabedoria que é a qualidade essencial requerida para alcançar Nibbana.<o:p></o:p></span></p>
<p class=Normal>Webu Sayadaw não era um acadêmico e os seus discursos não estão dirigidos para o intelectual que prefere o estudo da filosofia Budista à prática. A sua simplicidade estimulante, paciência, seu senso de humor e a sua humildade, todas reveladas nos seus diálogos com a audiência, iluminam um lado do Budismo que não pode ser notado através da leitura de tratados e textos. As observações feitas pelos membros da audiência nos oferecem uma breve perspectiva de como o Budismo é praticado na Birmânia nos dias de hoje.<o:p></o:p></span></p>
<p class=Normal>Webu Sayadaw realizou peregrinações aos locais sagrados do Budismo na Índia e no Sri Lanka. Ele faleceu em 26 de Junho de 1977 no centro de meditação no seu vilarejo natal Ingyinbin. Muitos acreditam que ele era um arahant. <o:p></o:p></span></p>

