<p class=Normal>1. Como são as pessoas iluminadas? Bem, alguns são homens e algumas são mulheres. 2 Você poderia encontrá-los em um monastério ou numa casa, na floresta ou em uma pequena cidade do interior. É verdade que não há muitos deles, mas há muito mais do que as pessoas costumam pensar. Não é que a iluminação é inerentemente difícil; a triste verdade é que a maioria das pessoas não quer ter o incômodo de se dar ao trabalho de se erguerem do pântano da ignorância e do desejo. 3</p>

<p class=Normal>2. Em princípio você não notaria a pessoa iluminada no meio da multidão, porque ela é bastante calma e discreta. Mas quando as coisas começarem a esquentar, é quando ela se destaca. Quando todo mundo estiver inflamado pela raiva, ela ainda estará cheia de amor. 4 Quando os outros estiverem em tumulto
por causa de alguma crise ela estará tão calma como antes. 5 Em uma corrida louca para obter o máximo possível, ela será a única em um canto com a expressão de contentamento em seu rosto. 6 Ela caminha suavemente sobre a aspereza, 7
ela é estável em meio à instabilidade. 8 Não é que ela faz questão de ser diferente, mas é a liberdade do desejo que a faz completamente auto-controlada. 9
Mas, estranhamente, embora outros não possam movê-la, sua presença calma os move. Suas gentis e equilibradas palavras unem aquelas em desacordo e aproximam ainda mais aquelas que já estão unidas. 10 Os aflitos, os medrosos e os preocupados se sentem melhor depois de ter conversado com ela. 11 Os animais selvagens sentem a bondade no coração da pessoa iluminada e não têm medo dela. 12 Até mesmo o lugar onde ela permaneça, seja aldeia, floresta, montanha ou vale, parece mais encantador, simplesmente porque ela está lá. 13</p>

<p class=Normal>3. Ela não está sempre expressando uma opinião ou defendendo um ponto de vista, na verdade, ela não parece ter quaisquer pontos de vista, 14 por conseguinte, as pessoas costumam confundi-la com um idiota. 15 Quando ela não se aborrece ou retalia os abusos ou ridicularizações, novamente as pessoas pensam que deve haver algo errado com ela. Mas ela não se importa com o que pensam. Ela parece ser idiota, mas é só que ela prefere permanecer em silêncio. Ela age como se fosse cega, mas na verdade ela vê tudo o que está acontecendo. As pessoas pensam que ela é fraca, mas realmente ela é muito forte. 16 Apesar de todas as aparências, ela é tão afiada quanto o fio de uma navalha. 17</p>

<p class=Normal>4. Seu rosto é sempre radiante e sereno porque ela nunca se preocupa com o que aconteceu ontem ou o que poderá acontecer amanhã.18 Seu porte e movimentos são graciosos e equilibrados, porque ela tem natural atenção plena em tudo que faz. 19 Sua voz é linda de ouvir e suas palavras são afáveis, claras, e diretas ao ponto.
20 Ela é bela de uma forma que tem nada a ver com a aparência física ou eloqüência, mas que vem da sua própria bondade interior. 21</p>

<p class=Normal>5. Ela pode ter uma casa, mas se esta queimar amanhã ela mudaria para outro lugar e ali se sentiria tão confortável quanto antes. Ela se sentirá em casa em qualquer lugar. 22 Mesmo aquelas que tentam reduzir o número de coisas que possuem sempre parecem ter demais. Não importa quanto a pessoa iluminada receba ela sempre parece ter apenas o suficiente. É verdade, que ela busca satisfazer as necessidades da vida como todo mundo, mas ela toma apenas o que precisa e suas necessidades são muito pequenas. Sua vida é organizada e simples, ela se contenta com o que venha no seu caminho. 23 Sua alimentação real é a alegria, 24 sua bebida real é a verdade, 25 sua verdadeira permanência é a diligência. 26</p>

<p class=Normal>6. As pessoas comuns são tão barulhentas como riachos borbulhantes, enquanto a pessoa iluminada é tão silenciosa como as profundezas do oceano. 27 Ela ama a calma e fala em louvor de calma. 28 Isso não quer dizer que ela nunca abre a boca. Ela se sentirá muito feliz em falar sobre o Dhamma para aqueles que estiverem interessados em escutar, 29 muito embora ela nunca catequize e nunca irá se envolver em discussões ou debates. 30 Além disso, como ela não fala além do que realmente sabe, 31 tudo o que disser está suportado por uma autenticidade que os 'especialistas' simplesmente não podem igualar. </p>

<p class=Normal>7. A mente da pessoa iluminada não está cheia de pensamentos nem é inativa. 32 Quando ela precisa de pensamentos, ela pensa, e quando não precisa deles, permite que se calem. 33 Para ela os pensamentos são uma ferramenta, não um problema. Ela ainda tem memórias, emoções e idéias, mas é indiferente a todas. Para ela, são apenas ilusões mágicas. 34 Ela observa como surgem, como persistem, como passam. 35 Sua mente é como o límpido céu vazio 36 - as nuvens passam flutuando mas está permanece espaçosa, límpida e inalterada. </p>

<p class=Normal>8. Embora ela seja pura de todas as formas, a pessoa iluminada não pensa de si mesma como sendo superior, igual ou inferior a ninguém. 37 Outros são tal como são, e não há necessidade de julgamentos ou comparações. Ela não é a favor ou contra alguém ou alguma coisa. 38 Ela não vê mais as coisas em termos de bem e mal, puro e impuro, sucesso e fracasso. 39 Ela entende o mundo da dualidade 40 tendo ido além disso. Ela até mesmo está além da idéia de samsara e nibbana. 41 Estando além de tudo, ela está livre de tudo. Sem desejos, sem medos, sem conceitos, sem preocupações. </p>

<p class=Normal>9. Não há muito tempo a pessoa iluminada estava tão confusa e tão infeliz quanto todos os outros. Assim como ela realizou isso? Realmente foi muito simples. Ela parou de buscar a causa de todo seu sofrimento fora de si mesma e começou a olhar para dentro de sei mesma. 42 Ao olhar, viu que as coisas com as quais ela se identificava e se apegava: corpo, sentimentos, emoções, conceitos, problemas, todos não são seus. 43 E então ela simplesmente soltou daquilo. Não mais embaraçada no irreal ela viu o real, o Não-nascido, o Não-tornado, o Não-feito, o Incondicionado. 44 Agora ela permanece nessa libertação no vazio e sem sinais 45 e ela está feliz o tempo todo. 46 Por isso, é bastante difícil de categorizar a pessoa Iluminada. Outros tentam limitá-la chamando-a de santo, Arahant ou mesmo às vezes tola. Mas ela ri desses rótulos e refere-se a si mesma como "um ninguém". 47 Como é possível rotular alguém que transcendeu todos os limites? 48</p>

<p class=Normal>10. Como ela completou a sua tarefa e não tem mais nada para fazer 49 a pessoa iluminada passa a maior parte de seu tempo sentada calmamente cuidando dos seus afazeres. 50 A pessoa comum pode pensar que parece haver uma tediosa semelhança  com a vida de uma pessoa iluminada. "Quero um pouco de emoção, um pouco de variedade", dizem elas. Mas é claro que quando chega a emoção ou a variedade do tipo que elas não querem - doença, fracasso, rejeição ou morte - elas então caem no desespero. É quando a pessoa iluminada silenciosamente se adianta para ajudar e curar. E como ela tem tempo de sobra, pode dar-se inteiramente aos outros. Ela toca a todos com seu amor. 51</p>

<p class=Normal>11. Ela está feliz em permanecer assim até o final e quando a morte finalmente vier, ela irá abraçá-la sem medo e seguirá o seu caminho sem arrependimentos. 52 O que acontece com a pessoa Iluminada após a morte? Os estudiosos têm argumentado sobre isso há séculos. Mas não é possível encontrar para onde foi o Iluminado, da mesma forma que é impossível traçar os rastros de pássaros que cruzam o espaço livremente . 53 Na morte como na vida, Aquele sem Rastros não deixa rastros. 54</p>

