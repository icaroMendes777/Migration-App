<p class=Normal>O vazio é como o espaço. Vazio significa nenhuma coisa (no-thing em inglês). Por exemplo, para que este lugar fique vazio, o que precisamos fazer? Temos que nos livrar de tudo, certo? As pessoas têm que sair daqui, todas as coisas que foram aqui colocadas devem ser retiradas, assim este lugar ficará vazio.</p>

<p class=Normal><i>Nibbana</i> é o vazio da mente. A mente está cheia de tantas coisas: pensamentos, desejos, emoções. Essas coisas entulham a mente. Eles tornam a mente infeliz. O Buda disse que temos que nos livrar de todas as coisas que ocupam espaço na nossa mente. Temos que limpar tudo da mente, ou seja, limpar todos os nossos desejos, nossos anseios, nossa ganância, nossa raiva, nossa delusão, nossos apegos e medos. Temos que nos livrar de tudo o que temos na mente, sendo que a maneira de fazer isso é meditando.</p>

<p class=Normal>Quando meditamos, concentramos a mente num objeto, como por exemplo a respiração. Se pudermos manter a concentração na respiração, a mente gradualmente irá parar de criar todos esses pensamentos e emoções. Tudo na mente é criado pela própria mente - pela volição e pela memória. Pensamos, lembramos e assim começamos a criar todos os tipos de sentimentos e emoções sobre as coisas que pensamos. Quando paramos de pensar, tudo desaparece, assim a mente fica vazia.</p>

<p class=Normal>Entrando em <i>jhana</i> é quando a mente fica vazia. No entanto, existe uma parte da mente que sempre irá permanecer, não irá desaparecer. O que resta é a paz e a felicidade. Isto é o que o vazio significa - o vazio da mente.</p>

<p class=Normal>O que precisamos fazer é meditar. Quando meditamos, deixamos de criar todas as coisas que confundem a mente, que são as volições ou os pensamentos. Podemos pará-los usando a atenção plena (<i>sati</i>), concentrando a mente num objeto. Para a meditação sentada, o Buda  recomendou usar a respiração como objeto de meditação, de modo a impedir a mente de pensar, de criar todas as coisas que entulham a mente.</p>

<p class=Normal>Então, se pudermos fazer isso, quando a mente parar de gerar pensamentos, ficará vazia. Quando fica vazia, a mente entra em <i>jhana</i>. Torna-se calma e pacífica. Não tem emoção, nem raiva, nem amor, nem medo, nem delusão. Este é o primeiro passo para criar o vazio na mente, criar <i>suññata</i>. Mas esta é uma situação temporária porque saindo da meditação a mente começa a gerar todos os tipos de pensamentos novamente. Quando vemos alguma coisa ou quando ouvimos alguma coisa, começamos a gerar desejos ou anseios, começamos a ter gostos ou desgostos novamente.</p>

<p class=Normal>O próximo passo a ser feito, se quisermos nos livrar das coisas que entulham a mente, é usar a sabedoria ou o insight, que o Buda descobriu ser o que ensina a mente a parar de reagir ao que entra em contato. Ensinando a mente a ver que tudo com o que entra em contato é impermanente, pode causar sofrimento ou tristeza. Não vamos poder controlá-la ou administrá-la para sempre nos proporcionar felicidade, porque tudo vem e vai. Tudo surge e cessa. Tudo é impermanente.</p>

<p class=Normal>Então, se pudermos ver que todas as coisas com as quais a mente entra em contato são <i>anicca</i> (impermanente), <i>dukkha</i> (causa tristeza ou sofrimento), <i>anatta</i> (não está sob o nosso controle), então sabemos que é melhor não se envolver com elas. Apenas as conhecemos, mas não temos nenhum apego por elas. Essa é a sabedoria que o Buda descobriu de modo a esvaziar a mente.</p>

<p class=Normal>Então, a mente estará vazia de pensamentos e emoções, independentemente do que entre em contato com a mente. A mente estará vazia tal como fica em <i>samadhi</i> ou <i>jhana</i>, sem estarmos em <i>jhana</i>. Esta é a maneira de esvaziar a mente permanentemente. Uma vez que tenhamos essa sabedoria, sempre poderemos ensinar a mente a não gerar nenhum tipo de emoção, nenhum tipo de amor, raiva, medo ou delusão. Assim a mente estará vazia.</p>

<p class=Normal>Portanto são dois passos. O primeiro passo é meditar, para ver como é quando limpamos a mente de todos os entulhos. Descobrimos que sem nada ficamos muito mais felizes. O próximo passo é quando saímos da meditação e entramos em contato com as coisas, ensinamos a mente que é melhor não ter nada ou se envolver com coisas que podem entulhá-la pois causam mais sofrimento do que felicidade. É melhor estar vazia. Pois estar vazia é mais feliz do que ter algo. Isso é sabedoria.</p>

<p class=Normal>Essa sabedoria ensinará a mente a abrir mão de tudo e não desejar ou ansiar por nada. Assim, embora a mente ainda entre em contato com as coisas e as pessoas, ela tem uma atitude diferente. A mente os toma pelo que são. Vêm e vão. Surgem e cessam. O que quer que aconteça não causa sofrimento à mente, porque a mente não tem apego. Não tem desejo,  podem vir e podem ir; podem desaparecer; podem morrer, porque essas são as coisas que não podemos controlar. É como a natureza. Não podemos controlar a natureza. Não podemos controlar o sol, a chuva, o vento, mas podemos conviver com alegria. Isto é ensinar a mente e esvaziar-se por completo.</p>

