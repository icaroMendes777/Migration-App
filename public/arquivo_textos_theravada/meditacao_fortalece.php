<p class=Normal>Pesquisadores americanos descobriram mais evidências de que meditar fortalece o cérebro. Estudos anteriores feitos pela Universidade da Califórnia (UCLA), nos Estados Unidos, já haviam sugerido que meditar durante anos torna o cérebro mais espesso e fortalece conexões entre células cerebrais. </p>

<p class=Normal>As novas pesquisas feitas pela mesma equipe californiana revelaram ainda mais benefícios associados à prática. Os resultados foram publicados pela revista Frontiers in Human Neuroscience em 29 de Fevereiro de 2012. </p>

<p class=Normal>A cientista Eileen Luders e seus colegas do Laboratory of Neuro Imaging da UCLA dizem ter encontrado indícios de que pessoas que meditam durante muitos anos têm quantidades maiores de dobras no córtex cerebral do que pessoas que não meditam. Isso poderia acelerar o processamento de informações. </p>

<p class=Normal>A equipe também encontrou uma relação direta entre a quantidade de dobras e o número de anos durante os quais a pessoa meditou. Isso pode talvez ser mais uma prova da neuroplasticidade do cérebro - a habilidade do órgão de se alterar, ou se adaptar, em resposta a estímulos externos. </p>

<p class=Normal><b>Córtex</b></p>
<p class=Normal>O córtex é a camada externa do cérebro e tem papel fundamental na memória, atenção, pensamento e consciência. Os dobramentos corticais são o processo pelo qual a superfície do cérebro se altera para criar sulcos e dobras. Sua formação pode promover e melhorar os processos nervosos. </p>

<p class=Normal>Presume-se, portanto, que quanto mais dobras se formam, maior a capacidade do cérebro de processar informações, tomar decisões e formar memórias. "Em vez de simplesmente comparar pessoas que meditam com as que não meditam, queríamos ver se havia uma relação entre a quantidade de prática da meditação e o grau de alteração do cérebro", disse Luders. "Quer dizer, associar o número de anos de meditação com a incidência das dobras".</p>

<p class=Normal><b>Testes</b></p>
<p class=Normal>Os pesquisadores fizeram exames de ressonância magnética em 50 praticantes de meditação - 28 homens e 22 mulheres. Esse grupo foi comparado a outro, de não praticantes, com idade e sexo equivalentes. Os praticantes haviam meditado em média 20 anos. Os tipos de meditação eram variados, entre eles a meditação Vipassana. </p>

<p class=Normal>A equipe disse ter encontrado grandes diferenças na incidência das dobras em participantes que praticavam meditação. Para os pesquisadores, a revelação mais interessante foi a correlação positiva entre o número de anos de meditação e a quantidade de dobras, especialmente em uma estrutura do cérebro conhecida como ínsula. </p>


<p class=Normal><b>Emoção e Cognição</b></p>

<p class=Normal>Sabe-se que a ínsula está associada às emoções humanas. E que lesões nessa estrutura podem resultar em apatia, perda de libido e alterações na memória. </p>

<p class=Normal>"Talvez (a descoberta) mais interessante tenha sido a relação positiva entre o número de anos de meditação e a quantidade de dobramentos insulares".</p>

<p class=Normal>Embora ainda falta determinar as exatas implicações funcionais dos maiores dobramentos corticais, estas descobertas sugerem que a insula é uma estrutura chave no processo de meditação. Por exemplo, as variações na complexidade da ínsula podem influenciar o controle das bem conhecidas distrações no processo de meditação como por exemplo sonhar acordado, distrações e projetar-se ao passado ou ao futuro. </p> 

<p class=Normal>Além disso, dado que os meditadores são conhecidos como mestres em introspecção, atenção e controle emocional, a maior incidência das dobras na ínsula refletem a integração dos processos autonomicos, emocionais e cognitivos. </p>

<p class=Normal>Luders adverte que fatores genéticos e ambientais podem ter contribuído para os efeitos observados. Ainda assim, "a relação positiva entre as dobras e o número de anos de prática dá suporte à ideia de que a meditação aumenta a incidência das dobras".</p>

