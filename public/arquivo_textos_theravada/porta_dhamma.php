<p class=Normal>Ao mesmo tempo, ao demonstrar respeito por
aqueles que conquistaram a proficiência no caminho, você também estará
mostrando respeito pelas qualidades que quer desenvolver em si mesmo. E quando essas
pessoas virem que você respeita tanto as boas qualidades deles como as suas
próprias, elas se sentirão mais inclinadas a compartir a sabedoria delas com
você e serão mais cuidadosas, compartilhando apenas o melhor que possuem. É por
isso que a tradição Budista coloca tanta ênfase não apenas em <i>sentir </i>respeito
mas também em <i>mostrar </i>respeito. Se você não puder esforçar-se para
demonstrar respeito em relação aos outros para que eles possam reconhecê-lo,
existe um elemento de resistência na sua mente. Eles, por outro lado, terão
dúvidas sobre o seu interesse em aprender. Essa é a razão porque a disciplina
monástica coloca tanta ênfase no protocolo de respeito a ser demonstrado para
mestres e monges sênior.<o:p></o:p></span></p>

<p class=Normal>Os ensinamentos sobre o respeito, entretanto,
se encaminham numa outra direção também. Os monges e monjas Budistas são
proibidos de demonstrar desrespeito por qualquer pessoa<i> </i>que os critique,
não importando se eles são iluminados ou não, ou se a crítica está
fundamentada. Críticos desse tipo podem não merecer o nível de respeito devido
aos mestres, mas eles merecem a cortesia ordinária. Isso porque, como explicado
acima, os padrões de causa e efeito estão presentes na consciência de todos e
mesmo pessoas não iluminadas podem ter observado fragmentos valiosos da
verdade. Se você se abrir para a crítica, poderá ouvir alguns insights valiosos
que um grande desrespeito teria repelido. A literatura Budista – desde os
primeiros tempos até o presente – abunda com estórias de pessoas que alcançaram
a iluminação após ouvir uma palavra ao acaso ou uma verso de uma fonte
improvável. Uma pessoa com a atitude de respeito adequada pode aprender com
tudo – e a habilidade de fazer bom uso de qualquer coisa é uma marca da
verdadeira sabedoria.<o:p></o:p></span></p>

<p class=Normal>Talvez a habilidade mais delicada em relação ao
respeito seja aprender como equilibrar todos os três aspectos do respeito: por
você mesmo, pela verdade da causalidade e pelo insight dos outros. Esse
equilíbrio, é claro, é essencial para qualquer habilidade. Se você quiser se tornar
um oleiro, por exemplo, você tem que aprender não só com o seu professor, mas
também com as suas ações e poderes de observação e com a própria argila. Além
disso, você terá que ponderar todos esses fatores em conjunto para alcançar a
sua maestria. Se, no desenvolvimento do caminho Budista, o respeito por você
mesmo tiver maior importância que o respeito pela verdade da causalidade ou os
insights dos outros, você achará difícil aceitar críticas ou se divertir com as
suas próprias tolices. Isso tornará impossível o aprendizado. Se, por outro
lado, o seu respeito pelos mestres tiver maior importância que o respeito por
você mesmo ou o respeito pela verdade, você irá se abrir para charlatões e
fechar-se para a verdade que o Cânone afirma “é para ser visto pelos sábios por
eles mesmos.”<o:p></o:p></span></p>

<p class=Normal> Os paralelos entre o papel do respeito na
prática Budista e nas habilidades manuais explica porque muitos mestres
Budistas requerem que os seus estudantes obtenham maestria numa habilidade
manual como pré-requisito ou como parte da sua meditação. Uma pessoa sem
habilidades manuais terá pouco entendimento intuitivo sobre como equilibrar o
respeito. O que separa as habilidades do Buda das demais, no entanto, é o nível
de total liberdade que elas produzem. E a diferença entre essa liberdade e a
sua alternativa – intermináveis ciclos de sofrimento com nascimento após
nascimento, morte após morte – é tão extrema que podemos com facilidade
compreender porque as pessoas que se entregaram à busca dessa liberdade
demonstram um nível de respeito e veneração para com essa liberdade que também
são extremos. Ainda mais compreensível é o nível absoluto de respeito e
veneração por essa liberdade demonstrado por aqueles que a alcançaram. Eles se
curvam para todos os seus mestres internos e externos com a mais sincera e
profunda gratidão. Vê-los curvarem-se dessa forma é algo inspirador.<o:p></o:p></span></p>

<p class=Normal>Portanto, quando pais Budistas ensinam aos seus
filhos que mostrem respeito pelo Buda, Dhamma e Sangha, eles não estão
ensinando-lhes um hábito que mais tarde deverá ser desaprendido. É claro, a
criança terá que aprender como compreender e fazer uso desse respeito da melhor
maneira possível, mas pelo menos os pais terão ajudado a abrir a porta para a
criança aprender através dos seus próprios poderes de observação, para aprender
com a verdade e aprender com os insights dos outros. E quando essa porta –
quando a mente – é aberta para aquilo que verdadeiramente merece respeito,
todas as coisas nobres e boas poderão entrar.<o:p></o:p></span></p>



