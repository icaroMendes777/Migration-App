<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Se você
nascesse em uma família Budista na Ásia, a primeira coisa que os seus pais lhe
ensinariam sobre o Budismo não seria um princípio filosófico mas um gesto de
respeito: como posicionar as suas mãos em <i>añjali, </i>palma com palma sobre
o coração, ao encontrar uma imagem do Buda, um monge ou uma monja. Obviamente,
no princípio o gesto será mecânico. Ao longo do tempo, no entanto, você iria
aprender a atitude de respeito que acompanha o gesto. Se você aprendesse com
rapidez, os seus pais considerariam isso como um sinal de inteligência pois o
respeito é básico em toda habilidade para o aprendizado.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ao crescer
um pouco mais, eles poderiam ensinar- lhe o simbolismo do gesto: que as suas
mãos formam um botão de uma flor de lótus, representando o seu coração, que
você está apresentando ao seu objeto de respeito na esperança de ser treinado
em como se tornar sábio. Por fim, à medida que você fosse se familiarizando com
os frutos da prática Budista, os seus pais esperariam que o seu respeito se
convertesse em reverência e veneração. Dessa forma, eles dão uma rápida
resposta à antiga questão do Ocidente sobre qual lado do Budismo – a filosofia
ou a religião – vem primeiro. Aos olhos deles, a atitude religiosa de respeito
é necessária para que a compreensão filosófica cresça. Quanto a eles, não
existe conflito entre ambos. Na verdade, eles se reforçam mutuamente.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso
representa um significativo contraste em relação à típica atitude Ocidental,
que enxerga uma discrepância fundamental entre os lados religioso e filosófico
do Budismo. A filosofia parece ser tão racional, colocando um valor tão elevado
na auto-confiança. O insight que se encontra no núcleo da iluminação do Buda é
tão abstrato – um princípio de causalidade. Parece que não existe uma razão
inerente para uma filosofia com um princípio tão abstrato ter produzido uma
devoção intensa o suficiente para se rivalizar com qualquer coisa encontrada
nas religiões teístas.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>No entanto,
se virmos o que o Cânone em Pali tem a dizer sobre a devoção – a atitude
expressa com o grupo de palavras <i>respeito, deferência, reverência, homenagem
e veneração </i>– não só aprenderemos que a teoria sobre o respeito nele
contida está enraizada no insight central da iluminação do Buda – o princípio
causal denominado condicionalidade isto/aquilo <i>(idappaccayata) </i>– mas
também que o respeito é, em primeiro lugar, essencial para aprender e obter a
maestria desse princípio causal.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Superficialmente,
pode parecer estranho relacionar uma teoria de causalidade com o tema de
respeito, mas os dois estão inter-relacionados de forma íntima. Respeito é a
atitude que você desenvolve em relação às coisas que são importantes na vida.
As teorias de causalidade revelam se algo realmente tem importância, e se assim
é, <i>o que e como </i>importam<i>. </i><span style='mso-bidi-font-style:italic'>Tomemos
por </span>exemplo o <i>o que:</i> se você crê que um ser superior irá
conceder-lhe a felicidade, você naturalmente irá mostrar respeito e reverência
para com esse ser. Se você assumir que a felicidade é totalmente baseada na
vontade própria, seu maior respeito será reservado para a sua própria vontade.
Quanto ao <i>como:</i> Se você considerar que a verdadeira felicidade é
totalmente impossível, totalmente predeterminada ou totalmente aleatória, não
existe necessidade de respeito, pois isso não fará diferença no resultado da
sua vida. Mas se você considerar que a verdadeira felicidade é possível e que
as suas causas são instáveis, contingentes e dependentes da sua atitude, você
obviamente irá demonstrar o cuidado e respeito necessários para mantê-las
fortes e saudáveis.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso está
refletido na forma como o Cânone trata o tema do respeito. Ele demonstra com
riqueza de detalhes as variadas formas pelas quais os discípulos leigos
contemporâneos do Buda demonstravam respeito ao Buda e à Sangha monástica e as
formas mais padronizadas através das quais os membros da Sangha demonstravam o
seu respeito ao Buda e entre si. De especial interesse é o protocolo de
respeito que cerca o ensinamento do Dhamma. Os monges e monjas Budistas são
proibidos de ensinar o Dhamma a qualquer um que demonstre desrespeito e diz- se
que o próprio Buda recusou ensinar o seu primeiro sermão ao grupo de cinco
ascetas até que eles parassem de tratá-lo como um mero igual.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Esse
protocolo, é claro, pode ter sido um mero acidente cultural, algo tomado meio a
contragosto da sociedade na época do Buda, mas existem trechos no Cânone que
sugerem algo distinto. O Budismo era um dos movimentos de contemplativos (<i>samana</i>)
na antiga Índia, que afirmava seguir as regras da natureza ao invés das regras
culturais predominantes. Esses movimentos tinham muita liberdade sobre o que
adotar dos costumes prevalecentes. As descrições Budistas de outros movimentos
samana freqüentemente mostravam críticas por eles serem desrespeitosos não
somente em relação a pessoas de fora mas também entre si mesmos. Os estudantes
são mostrados como desrespeitosos para com os seus mestres, as reuniões em
grupo são malcriadas, ruidosas e sem controle. Tudo isso é então contrastado
com a forma como os Budistas conduzem as suas reuniões com cortesia mútua e
respeito. Isso sugere que os Budistas tinham liberdade para rejeitar os
costumes públicos de respeito, mas escolheram conscientemente não fazer isso.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Essa<span
style="mso-spacerun: yes">  </span>escolha está baseada no insight de que o
respeito é um pré-requisito para o aprendizado. Será mais fácil você aprender
com alguém por quem sinta respeito do que com alguém por quem não o sinta. O
respeito é aquilo que abre a sua mente e afrouxa as suas opiniões preconcebidas
para abrir espaço para novos conhecimentos e habilidades. Ao mesmo tempo, uma
pessoa que tem um ensinamento valioso a oferecer irá se sentir mais inclinada a
ensinar para alguém que demonstre respeito do que para alguém que não o
demonstre.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>No entanto,
o tipo de aprendizado que o Buda enfatiza não é a simples aquisição de
informação. É um treinamento que lhe permite obter a maestria sobre outras
habilidades que o conduzirão à total libertação do sofrimento e estresse. E é
nesse ponto que o tema do respeito se conecta com a causalidade, pois a teoria
Budista da causalidade está centrada na questão sobre como é possível aprender
uma habilidade.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A
cibernética demonstra que o aprendizado somente é possível quando existe
feedback; para aprender uma habilidade é necessário ter a capacidade para
monitorar o feedback e escolher como usá-lo para modificar o comportamento. As
descobertas do Buda sobre a causalidade explicam o <i>como </i>e o <i>que </i>tomando
em conta esse fator. O <i>como </i>ele expressou numa fórmula causal; o <i>que,</i>
numa análise da ação: os fatores condicionantes, juntamente com a variedade de
resultados que podem ser produzidos.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A fórmula
causal, colocada de forma simples, afirma que cada momento é composto de três
coisas: resultados de ações passadas, ações presentes e o resultado imediato de
ações presentes. Embora esse princípio pareça ser simples, as suas
conseqüências são bastante complexas. Cada ação que você realiza tem
repercussões no momento presente e também reverbera no futuro. Dependendo da intensidade
do ato, essas reverberações podem durar pouco ou muito tempo. Assim, cada
experiência condicionada é moldada pelo efeito combinado de ações passadas que
provêm de uma grande extensão ao longo do tempo, combinada com os efeitos dos
atos no presente.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A
causalidade ao longo do tempo coloca certas limitações em cada momento. O
presente não é uma folha em branco, pois ele é parcialmente moldado pelas
influências do passado. A causalidade imediata no presente, no entanto, abre
espaço para o livre arbítrio. Nem tudo é determinado pelo passado. A cada
instante você pode inserir algo novo no processo e empurrar a sua vida numa
nova direção. No entanto, não existe tanto espaço para o livre arbítrio que
faça com que a causalidade se torne arbitrária. Cada <i>isto </i>específico na
fórmula de causalidade isto/aquilo – quer esteja atuando ao longo do tempo ou
no presente imediato – está sempre associado com um <i>aquilo </i>específico.
Os eventos seguem padrões previsíveis em relação aos quais se pode obter
proficiência.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O <i>o que </i>que
mantém esse processo em movimento é o fator que possibilita o feedback e o
monitoramento do feedback. O elemento primário no <i>o que </i>é a intenção,
que o Buda identificou como sendo a essência da ação ou kamma. A intenção, por
seu lado, é moldada pela atenção que questiona as percepções e cria idéias a
partir dessas questões. Como você é capaz de ter consciência dos resultados das
suas intenções, existe o feedback na sua mente permitindo que você aprenda.
Sendo a atenção capaz de fazer perguntas, ela pode monitorar o feedback para
determinar a melhor maneira de usá-lo. E como as suas intenções – guiadas pelas
idéias, adicionam novos dados ao presente – podem então transformar a sua
experiência, a sua habilidade para aprender pode mudar a situação: você poderá
mudar o seu comportamento e colher os resultados das suas habilidades
aprimoradas em termos de uma felicidade cada vez maior.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Até onde
pode chegar essa felicidade? Através do seu processo de iluminação, o Buda
descobriu que a busca da habilidade pode no fim conduzir totalmente para além
do ciclo de renascimentos. A partir dessa descoberta ele identificou quatro
tipos de kamma: os três primeiros proporcionando resultados prazerosos,
dolorosos ou mistos no ciclo de renascimentos e o quarto conduzindo para além
de kamma, para o fim do ciclo de renascimentos. Em outras palavras, o princípio
de causalidade atua de forma que as ações possam ou dar continuidade ao ciclo
de renascimentos, ou dar um fim a ele. Como os prazeres mais refinados dentro
do ciclo são inconstantes e indignos de confiança, ele ensinou que o curso de
ação mais digno é o quarto tipo de kamma – o tipo que o levou à iluminação –
que dá um fim ao kamma de forma definitiva.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A
habilidade incorporada neste tipo de kamma provém da coordenação dos fatores da
atenção e da intenção para que eles possam conduzir primeiro a resultados
prazerosos dentro do ciclo de renascimentos e depois – num nível transcendente
– à completa libertação do sofrimento e estresse. Isso, por outro lado, requer
certas atitudes em relação ao princípio de causalidade que ocorre na vida
humana. E aqui a qualidade do respeito se torna essencial, pois sem o respeito
adequado por três coisas – você mesmo, o princípio da causalidade que ocorre na
sua vida e o insight de outras pessoas sobre esse princípio<span
style="mso-spacerun: yes">  </span>– você não será capaz de juntar a
determinação necessária para obter a proficiência desse princípio e ver até
onde pode chegar o seu potencial para a habilidade.<o:p></o:p></span></p>

<p class=Normal><i><span lang=PT-BR style='mso-ansi-language:PT-BR'>Respeito
por você mesmo, </span></i><span lang=PT-BR style='mso-ansi-language:PT-BR'>no
contexto da condicionalidade isto/aquilo quer dizer duas coisas:<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> 1) Como o
quarto tipo de kamma é possível, você pode ter respeito pelo desejo de obter a
felicidade não condicionada e não precisa considerá-la como uma idéia irreal.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> 2) Devido à
importância da intenção e atenção em moldar a sua experiência, você pode ter
respeito pela sua capacidade de desenvolver as habilidades necessárias para
compreender e obter proficiência da realidade causal, até o ponto em que
alcance a verdadeira felicidade.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Mas o respeito
por você mesmo vai ainda mais além disso. Você não só <i>pode </i>ter respeito
pelo desejo de obter a verdadeira felicidade e pela sua habilidade para
alcançá-la, mas <i>precisa </i>respeitá-las se não quiser ser vítima das muitas
forças religiosas e seculares dentro da sociedade e de você mesmo, que o
arrastariam em outras direções.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Embora a
maioria das culturas religiosas aceitem a verdadeira felicidade como possível,
elas não vêem a habilidade humana como capaz de realizá-la. Em termos gerais,
elas colocam a esperança de obter a felicidade em poderes mais elevados. Quanto
às culturas seculares, elas não acreditam que a felicidade não condicionada
seja possível. Elas nos ensinam que devemos nos esforçar pela felicidade
condicionada e fechar os olhos para as limitações inerentes à felicidade
advinda do dinheiro, poder, relacionamentos, posses ou a uma noção sentimental
de comunidade. Elas zombam com freqüência dos valores mais elevados e sorriem
quando ídolos religiosos caem ou quando aspirantes religiosos mostram ter pés
de barro.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='text-transform:uppercase;mso-ansi-language:
PT-BR'>E</span><span lang=PT-BR style='mso-ansi-language:PT-BR'>ssas atitudes
seculares estimulam as nossas próprias qualidades inábeis, nosso desejo de
tomar qualquer prazer que possa ser obtido facilmente e a nossa impaciência com
qualquer um que nos diga que somos capazes de mais e melhor. Mas tanto as
atitudes religiosas como as seculares nos ensinam a subestimar os poderes dos
nossos estados mentais hábeis. Qualidades como a atenção plena, concentração e sabedoria,
quando surgem na mente pela primeira vez, não parecem ser dignas de nota –
pequenas e frágeis, como mudas de uma árvore crescendo em meio às ervas
daninhas. Se não cuidarmos delas ou não lhes concedermos um respeito especial,
as ervas daninhas irão estrangulá-las ou nós mesmos acabaremos pisoteando-as.
Como resultado, nunca saberemos quanta sombra elas poderiam proporcionar.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Se, no
entanto desenvolvermos grande respeito pela nossa habilidade para alcançar a
verdadeira felicidade, duas importantes qualidades morais irão dominar as
nossas mentes e cuidar das nossas boas qualidades: o temor em relação ao
sofrimento, que experimentaremos, se não tentarmos desenvolver tal habilidade e
a vergonha ao pensar que estamos buscando algo que seja menos do que a
felicidade mais elevada possível. Vergonha pode ser um estranho apêndice ao
respeito por si mesmo, mas quando ambos são saudáveis eles caminham juntos.
Você precisa do respeito por si mesmo para reconhecer quando uma linha de ação
está abaixo de você e de sentir vergonha de seguí-la. Você precisa sentir
vergonha dos seus erros para evitar que o respeito por si mesmo se transforme
em orgulho teimoso. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>É nesse
ponto que o segundo aspecto do respeito – <i>respeito pelo princípio da
causalidade – </i>entra em cena. A condicionalidade isto/aquilo não é um
processo assimétrico. Cada <i>isto </i>inábil está conectado com um <i>aquilo </i>desagradável.
Você não será capaz de torcer a conexão para obter resultados agradáveis ou
usar as suas próprias preferências para desenhar um caminho feito ao seu gosto
para a libertação da experiência causal. O respeito por si mesmo, portanto, tem
que acomodar o respeito pela forma como as causas e efeitos na verdade se
comportam. De acordo com a tradição, esse respeito é expresso em relação à
qualidade que o Buda enfatizou nas suas últimas palavras: diligência. Ser
diligente significa ter a forte noção de que se as suas intenções forem
descuidadas, você sofrerá por isso. Se você realmente ama a si mesmo, tem que
prestar muita atenção à maneira como a realidade na verdade opera
e agir de acordo. Nem tudo aquilo que você pensa ou sente é digno de respeito.
O próprio Buda não esboçou o Budismo ou o princípio de condicionalidade
isto/aquilo. Ele o descobriu. Ao invés de ver a realidade de acordo com as suas
preferências, ele reordenou as suas preferências para obter o máximo daquilo que
ele havia aprendido ao observar – com escrúpulo e honestidade – as suas ações e
os seus efeitos.<o:p></o:p></span></p>

<p class=Normal>Esse ponto está refletido no discurso para os
Kalamas (<a href="../sutta/ANIII.65.php">AN III.65</a>). Embora esse discurso
seja citado com freqüência como a carta branca do Buda para que cada um siga a
sua própria noção do certo e errado, na verdade ele diz algo bem diferente: <i>Não
se deixem levar pelas tradições, mas também não se deixem levar por suas
preferências. Se vocês virem, através da observação das suas ações e dos seus
resultados, que ao adotar um certo estado mental este conduz ao mal e ao
sofrimento, vocês devem abandoná-lo e decidir nunca mais adotá-lo. </i>Este é
um padrão bastante rigoroso que exige que você coloque o Dhamma acima das suas
preferências pré-concebidas. E requer que você esteja muito atento a qualquer
tendência para reverter essa prioridade e colocar as suas preferências em
primeiro lugar.<o:p></o:p></span></p>

<p class=Normal>Em outras palavras, você não pode simplesmente
se entregar ao prazer ou resistir à dor que provém das suas ações. Você tem que
aprender tanto com o prazer como com a dor para respeitá-los como eventos de
uma cadeia causal, para ver o que eles têm para ensinar. É por isto que o Buda
chamou <i>dukha – </i>dor, sofrimento, estresse – de uma nobre verdade; e
porque ele também chamou o prazer que surge de uma mente concentrada de nobre
verdade. Esses aspectos da experiência imediata contêm lições que podem
conduzir a mente às realizações nobres. <o:p></o:p></span></p>

<p class=Normal>O discurso para os Kalamas, no entanto, não
pára na experiência imediata. Ele vai além e afirma que ao observar os
processos de causa e efeito nas suas ações, você também deve confirmar as suas
observações com os ensinamentos dos sábios. Esse terceiro aspecto do respeito –
<i>respeito pelo insight dos outros – </i>também está baseado no padrão da
condicionalidade isto/aquilo. Como as causas algumas vezes estão separadas dos
seus efeitos por um grande intervalo de tempo, é fácil perder de vista algumas
importantes conexões. Ao mesmo tempo, a qualidade mental que atua como
principal obstáculo à sabedoria – delusão – é aquela que é mais difícil de ser
detectada por você mesmo. Quando você está deludido, você não <i>sabe </i>que
está deludido. Portanto, a abordagem mais sábia é mostrar respeito pelos insights
dos outros, na eventualidade de que os insights deles possam ajudá-lo a ver a
sua própria ignorância. Afinal, a intenção e a atenção estão diretamente
presentes na consciência deles também. Os insights deles podem ser justamente
aquilo que você necessita para eliminar os obstáculos que você criou para si
mesmo através dos seus próprios atos ignorantes.<o:p></o:p></span></p>

<p class=Normal>Os ensinamentos Budistas sobre o respeito para
com as outras pessoas apontam em duas direções. A primeira direção é a mais
óbvia: respeito por aqueles que estão mais avançados no caminho do que você. O
Buda certa vez disse, a amizade com pessoas admiráveis é o todo da vida santa,
pois as palavras e exemplos delas irão ajudá-lo no caminho para a libertação.
Isso não quer dizer que você necessariamente tenha que obedecer os ensinamentos
delas ou aceitá-los sem pensar. Você apenas deve isso a si mesmo: tentar
ouví-los com respeito e testar os ensinamentos deles com honestidade. Mesmo – e
especialmente – quando o conselho deles for desagradável, você deve tratá-lo
com respeito. O Dhammapada 76 diz,<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> Considere-o
como aquele<br>

que
