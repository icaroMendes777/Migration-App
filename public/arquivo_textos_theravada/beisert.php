<p class=Tit1 align=center style='text-align:center'>Michael Beisert </Tit1>

<p class=Normal>Michael Beisert formou-se em Administração de Empresas e trabalhou boa parte da sua vida como executivo em empresas multinacionais, tanto no Brasil como no exterior. </p>

<p class=Normal>Em 1995 tomou contato com o Budismo o que de imediato despertou o seu interesse, até por que, na época ele já se sentia desencantado com a sua carreira profissional. </p>

<p class=Normal>Em 1996 participou de um retiro de meditação Vipassana na tradição de U Ba Khin e depois do retorno ao Brasil,  no início de 1998, participou de um retiro guiado pelo Bhante Gunaratana e tomou refúgio na Jóia Tríplice, recebendo o nome  Dhammarakkhita. </p>

<p class=Normal>Ele é um autodidata no Budismo, sendo que no ano 2000 fundou o site Acesso ao Insight com traduções de Suttas e textos budistas para o português. Em 2001 ele se aposentou para dedicar-se primordialmente aos estudos e à prática do Budismo. </p>

<p class=Normal>Além dos estudos e traduções, ele tem se dedicado à prática de meditação, tanto por meio da prática diária, como a participação em retiros. Participou de vários retiros na tradição Vipassana sob a orientação de professores leigos do IMS, com destaque para Joseph Goldstein, Cristine Feldman, Guy Armstrong, Myoshin Kelley e Carol Wilson. </p>

<p class=Normal>Também participou de retiros Vipassana sob a orientação do Venerável Gunaratana, Venerável Rahula, Sayadaw Rewata Dhamma, Sayadaw U Vivekananda e Venerável Ariya Ñani. </p>

<p class=Normal>Em 2004 e 2005 participou de retiros com ênfase na prática dos jhanas, sob a orientação de Leigh Brasington. </p>

<p class=Normal>Desde 2007 tem se dedicado a retiros pessoais solitários tanto no Forest Refuge do IMS, como no Vajrapani Institute, somando 19 meses em retiro. </p>

<p class=Normal>Em 2012 esteve num retiro pessoal solitário de 4 semanas no Jhana Grove sob a orientação de Ajaan Brahmavamso. </p>

<p class=Normal>Em 2011, 2012 e 2013 participou dos cursos sobre os estudos comparativos entre o Majjhima Nikaya e o Majjhima Agama dirigidos pelo Venerável Analayo. </p>


