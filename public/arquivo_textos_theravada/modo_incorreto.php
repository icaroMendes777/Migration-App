<p class=Normal>Os monásticos Budistas são chamados bhikkhus (monges) e bhikkhunis (monjas), que significa, literalmente, "homens e mulheres que dependem de esmolas para as necessidades da vida". Ou seja, eles dependem exclusivamente da generosidade dos leigos simpatizantes. No entanto, alguns monges, insatisfeitos com a quantidade e a incerteza de esmolas, tentam garantir um estilo de vida mais confortável, através da prática de vários ofícios ou profissões com a expectativa de uma remuneração. Isso é chamado modo de vida incorreto.</p>

 <p class=Normal>   Uma lista exaustiva de exemplos de modo de vida incorreto pode ser encontrada no <a href="../sutta/DN1.php">Brahmajala Sutta</a>. Essa lista inclui coisas como: quiromancia, adivinhação, interpretação de sonhos, determinação de datas ou locais propícios, exorcismo, recitação de encantos de proteção, recitação  de feitiços malévolos, prever o tempo restante de vida de uma pessoa, levar recados, mapas astrológicos, e assim por diante.</p>

<p class=Normal>    Mesmo aquilo que para um leigo seria um bom modo de ganhar a vida, como por exemplo praticar a medicina, é um modo de vida incorreto para um monge. Há uma história no Comentário do Dhammapada de um monge que se deu muito bem praticando a medicina. Certo dia esse monge recebeu uma deliciosa refeição de um homem que ele havia tratado. No caminho de volta ao monastério, ele encontrou com o Venerável Sariputta, um dos principais discípulos do Buda, e orgulhosamente falou do seu trabalho e lhe ofereceu algumas das suas iguarias.  O Venerável Sariputta ficou desalentado e foi embora sem dizer uma palavra. O Venerável Sariputta tinha uma grande estima pela tradição simples de depender de comida esmolada e ele não iria comer o que tivesse sido obtido através da quebra de preceitos, neste caso através da prática de um ofício para ganho pessoal. Ao retornar para o monastério ele relatou o incidente para o Buda, e o Buda recitou os seguintes versos:</p>

 <p class=Normal style='margin-left:.75in'> Fácil é  a vida de um desavergonhado,<br>
impudente como um corvo,<br>
maledicente, presunçoso, <br>
arrogante e corrompido. </p>
<p class=Normal style='margin-left:.75in'> Mas difícil é a vida de quem é modesto<br>
sempre em busca da purificação.<br>
Desapegado, humilde,<br>
vivendo imaculado e com discernimento.</br> (<a href="../dhp/dhp18.php">Dhp 244-245</a>)</p>

 <p class=Normal>   É permitido que um monge qualificado na medicina trate de pessoas que vivem no monastério, até mesmo para ajudar os visitantes - mas não é permitido fazer disso um negócio.</p>

<p class=Normal>    Há várias histórias em que o Buda, vendo uma oportunidade de estar a serviço, parava durante a coleta das esmolas de alimentos para dar um ensinamento inspirador para alguém. A pessoa então corria para dentro da casa para oferecer alguma coisa como uma "recompensa", mas o Buda sempre recusava. Ele explicou que os Iluminados não ensinam por honorários. Ensinar visando o ganho pessoal também é um modo de vida incorreto. O ensinamento é a maneira dos monásticos serem generosos, de servir, e isso nunca deve ser considerado como uma profissão, vender os ensinamentos para o ganho material. </p>

<p class=Normal>    Um monge corrupto que lê a sorte para ganhar dinheiro, dá palestras em troca de presentes, ou adota práticas semelhantes de modo de vida incorreto, tem um nome especial no Budismo - ele é chamado <i>allajji</i>, que significa "sem-vergonha". Bons monges são aconselhados a evitar os desavergonhados e se um grupo de monges <i>allajji</i> se reúnem em algum local, os bons monges devem ir até lá e mandá-los embora (tal como na regra Sanghadisesa 13).</p>

<p class=Normal>        Para enfatizar a gravidade da ofensa do modo de vida incorreto, há uma citação bem conhecida nos Suttas. 

 <p class=Normal>               "Monges, há essas quatro máculas devido às quais o sol e a lua não brilham, não radiam, não resplandecem. Quais são as quatro? Nuvens de chuva ... nuvens de neve ... fumaça e poeira ... e um eclipse. Também, monges, há essas quatro máculas devido às quais os monges não brilham, não radiam, não resplandecem. Quais são as quatro? Beber álcool ... entregar-se à relação sexual ... aceitar ouro ou dinheiro ... obter os requisitos através de um modo de vida incorreto. Estas são as quatro máculas devido às quais o sol e a lua não brilham, não radiam, não resplandecem." [<a href="#N1">1</a>]

<p class=Normal><a name=R1></a>    Fica portanto claro o que o Buda pensava sobre os monges <i>allajji</i> que para o ganho pessoal preveem os números da loteria, fazem mapas de astrologia e assim por diante. Eles fazem parte do mesmo grupo de monges que se embriagam, têm relações sexuais ou recebem dinheiro. Monges que praticam o modo de vida incorreto são uma mácula na Sangha e devem ser conhecidos como tal pela comunidade leiga.</p>

