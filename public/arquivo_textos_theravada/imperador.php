vendas irão crescer e com isso o nosso salário. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O imperador
na história original escapou de ser assassinado por ouvir atentamente o
conselho de um menino no caminho para visitar o eremita. O poderoso imperador
estava com uma mera criança, mas aquele menino era para ele a pessoa mais
importante no mundo e salvou a vida do imperador. Quando meus amigos vêm até
mim ao final de um longo dia para contar sobre os seus problemas, eu me lembro
da resposta do imperador para a segunda pergunta e lhes dou total importância.
Isso é abnegação. A compaixão provê a energia e funciona. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A
