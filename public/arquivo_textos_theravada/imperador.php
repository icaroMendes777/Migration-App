importante é aquela com a qual você está. Para muitas daquelas crianças foi <span
class=GramE>a</span> primeira vez na vida que elas se sentiram valorizadas,
especialmente por um adulto influente. Além disso, ao dar-lhes importância, ela
estava ouvindo completamente, ao invés de estar julgando. As crianças estavam
sendo ouvidas. O programa foi desenhado dessa forma. As crianças se sentiram
respeitadas e o programa funcionou. A minha palestra não foi a mais importante
no final das contas. Uma das crianças discursou depois de mim. Ele relatou a 
sua história de problemas familiares, drogas e crimes e como o programa havia 
trazido de volta a esperança para a sua vida e como em breve ele estaria indo 
para a universidade. No final eu estava com os olhos molhados. Esse foi o principal discurso. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Na maior
parte do tempo você está sozinho. Então, a pessoa mais importante, aquela com a
qual você está, é você mesmo. Há tempo suficiente para dar importância para si
mesmo. Quem é a primeira pessoa que você toma consciência ao despertar pela
manhã? Você! Alguma vez você <span class=GramE>disse,</span> “Bom dia, eu.
Aproveite o seu dia!”? Eu digo. Quem é a última pessoa da qual você tem
consciência quando vai dormir? Novamente você! Eu digo boa noite para mim
mesmo. Eu dou importância a mim mesmo nos muitos momentos em que estou só
durante o meu dia. E funciona. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A resposta
para a terceira questão do imperador, “Qual é a coisa mais importante a ser
feita?” É se importar. “Importar-se” engloba ser cuidadoso e compassivo. A
resposta ilustra que a coisa mais importante é o que temos em mente.<o:p></o:p></span></p>



