resposta correta. Quando li a resposta quando era um estudante, fiquei com
aquilo na cabeça por dias. A resposta projetou uma profundidade na questão que
eu jamais havia imaginado. A resposta é que a pessoa mais importante é aquela
com a qual você está. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Eu me recordei 
