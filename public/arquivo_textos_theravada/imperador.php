Eles pareciam ouvir, mas no íntimo queriam que eu fosse embora. Eles tinham coisas 
mais importantes para fazer. Isso é o
que eu sentia e essa sensação era muito ruim. Eu também me recordei de ter
criado coragem para me aproximar de um famoso palestrante e fazer uma pergunta
pessoal e ser agradavelmente surpreendido com a completa atenção dele. Outros
professores estavam esperando para falar como ele e eu era apenas um estudante
cabeludo, mas ele fez com que eu me sentisse importante. A diferença foi
enorme. </span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A
comunicação e o amor só podem ser compartidos quando a pessoa com a qual
