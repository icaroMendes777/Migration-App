advogados que tratam de divórcios teriam de procurar outro emprego se todas as 
pessoas envolvidas num relacionamento se lembrassem da resposta para a segunda 
pergunta do imperador e a colocassem em prática de modo que, sem importar quão 
cansados ou ocupados estejamos, quando estivermos com o nosso parceiro, faremos 
com que ele se sinta como se fosse a pessoa mais importante no mundo. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>No mundo
