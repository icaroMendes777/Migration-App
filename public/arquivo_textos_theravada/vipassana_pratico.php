<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Você deve
ter ouvido que uma pessoa deveria estar com a atenção plena o tempo todo, quer
esteja em casa ou no escritório, ou num ônibus ou no seu carro ou no carro de
um outra pessoa, etc. Esse conselho poderia ser interpretado como você tendo
que manter a mente focada o tempo todo na respiração. Ao dirigir, se você
mantiver a mente só na respiração provavelmente irá enfrentar alguns problemas,
tal como não dar atenção ao processo de dirigir e correr o risco de ter um
acidente.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Algumas
vezes você poderá pensar que “estar com atenção plena o tempo todo” significa
colocar a atenção apenas naquilo que você estiver fazendo naquele momento em
particular. Isso, é claro, é o que toda pessoa que leva o seu trabalho a sério
normalmente faz. Um artista, pintor, escritor, cantor, compositor, pensador,
orador, atirador, cozinheiro, etc. precisa prestar atenção àquilo que está
fazendo durante todo o tempo em que estiver engajado no seu trabalho.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Não são só
os seres humanos que fazem isso. Você deve ter<span style="mso-spacerun: yes"> 
</span>notado como os gatos prestam total atenção à sua presa para poderem
capturá-la, sem cometer nenhum erro que possa assustá-la. Tigres, leões,
crocodilos prestam total atenção àquilo que estão fazendo para capturar a sua
presa. Pode ser que você tenha notado que as garças permanecem paradas num
único lugar por um longo tempo para agarrar um peixe. Cães pastores ficam
totalmente atentos aos movimentos das ovelhas para que possam correr com
rapidez e dirigir o rebanho na direção certa. Infelizmente nem o gato, a garça
ou o cão pastor são capazes de remover a sua cobiça, paixão, etc., ou cultivar
um pouquinho que seja de insight prestando total atenção aos seus objetos.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Prestar
atenção ao que quer que seja que você esteja fazendo em todos os momentos não
irá eliminar o seu desejo, raiva e ignorância. Isso, na verdade, é exatamente o
que você faz na meditação da tranqüilidade ou concentração. Ao prestar atenção
a uma coisa de cada vez não será possível eliminar os irritantes psíquicos.
Você pode focar a sua mente em apenas um objeto durante cinqüenta anos e ainda
assim a irritação psíquica na sua mente permanecerá inalterada. Uma pessoa pode
observar todas as regras de comportamento virtuoso. Uma outra pode aprender de
memória todas as escrituras. Outra ainda,<span style="mso-spacerun: yes"> 
</span>pode obter a concentração. Outra pode passar toda a sua vida em solidão.
Todas elas podem pensar que serão capazes de experimentar a libertação suprema
de todos os irritantes psíquicos, que não está ao alcance da pessoa comum. Mas
nenhuma delas poderá ter esse tipo de experiência se não destruírem todos os
irritantes psíquicos. Então, em adição a tudo aquilo que elas praticam, elas
também precisam remover todas as suas impurezas psíquicas para que possam
experimentar a felicidade da emancipação de todos os tipos de dor. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O que fica
faltando ao focar a total atenção num único objeto durante todo o tempo é a
sabedoria. A sua atenção plena deve estar combinada com a atenção com sabedoria
ou reflexão com sabedoria <i>(yoniso manasikara)</i>. O que é atenção combinada
com a reflexão com sabedoria? É a atenção acompanhada pelas três raízes
benéficas. O que são as três raízes benéficas? Elas são a generosidade, o amor
bondade e a sabedoria. Isso significa que quando você prestar atenção em algo
tente sempre colocar a sua atenção sem o desejo, raiva ou delusão; com o
pensamento de generosidade, amor bondade e sabedoria. Essas três são chamadas
de raízes benéficas; o desejo, a raiva e a delusão são chamadas de raízes
prejudiciais. Não permita que a sua mente seja influenciada pelas raízes
prejudiciais ao prestar atenção em algo. Deixe que os pensamentos de
generosidade, amor bondade e sabedoria dominem a sua mente enquanto estiver
dando atenção a qualquer coisa.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ao prestar
atenção enquanto você lava as panelas, não é necessário pensar em termos de
generosidade, amor bondade e sabedoria em relação a elas. Você cultiva a
atenção plena não pelas panelas mas pelos seres vivos. Você deve prestar
atenção a qualquer pensamento que diga respeito a você ou a qualquer outro ser
vivo. Aplique a reflexão com sabedoria ao vestir a roupa, comer, beber, falar
com alguém, ouvir um som, ver um objeto, caminhar ou dirigir.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ao prestar total atenção
refletindo com sabedoria, o seu desejo, raiva e delusão se diluem, porque ao
refletir com sabedoria a generosidade, amor bondade e sabedoria são ativados.
Os seus pensamentos de generosidade, amor bondade e sabedoria têm o poder de
minimizar o seu desejo, raiva e delusão em qualquer atividade que você esteja
engajado. Ao prestar atenção em algo, sem refletir com sabedoria, você poderá
inadvertidamente desenvolver o desejo, raiva e delusão. Você pode, por exemplo,
ver um objeto. Aos seus olhos aquele objeto pode parecer atraente, belo ou
prazeroso ou ele pode não ser atraente. Naquele instante se você não refletir
com sabedoria, poderá acabar desenvolvendo o desejo ou aversão em relação a ele
ou poderá ter idéias absolutamente confusas a respeito dele. Ou poderá pensar
que o objeto é permanente ao invés de compreender que ele é impermanente; que
traz a satisfação ao invés da insatisfação; ou que possui um eu ao invés de
estar desprovido de um eu. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Você poderá então perguntar
como é que os seus pensamentos generosos podem eliminar os seus pensamentos
desejosos, visto que estes querem se apegar ao objeto ou agarrá-lo. Ao perceber
o objeto com desejo, a sua mente se apega a ele e não aceita nenhum pensamento
no sentido de abandonar esse desejo. Pode ser que você não queira nem tirar os
olhos do objeto. Na verdade, naquele momento a sua mente fica cega a qualquer
pensamento de generosidade. Mesmo que queira soltar-se do apego ao objeto você
assim o fará com grande relutância. Você pode sentir que é generoso. Mas a sua
generosidade servirá apenas para satisfazer o seu propósito desejoso: como
obter algo em troca, ou obter reconhecimento, ou ficar famoso pela
generosidade. O desejo possui um adesivo muito forte. Ao primeiro contato com o
objeto desejável a mente cola nele com rapidez. Soltar-se daquele objeto é tão
doloroso quanto cortar fora um membro ou algum pedaço do seu corpo, assim você
não consegue soltar o objeto da sua mente.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>É quando você realmente
precisa da atenção com sabedoria. É quando você precisa aprender a ver a
impermanência, insatisfação e ausência de um eu no objeto que você está
observando. A sua atenção com sabedoria indica que nem o objeto que você
percebe nem a sua sensação em relação ao objeto permanecem as mesmas, nem mesmo
por dois momentos consecutivos. Você não terá a mesma sensação mais tarde. Você
muda, o objeto que você percebe muda. Com a atenção com sabedoria você irá ver
que tudo é impermanente. Esse entendimento da impermanência permite que você se
solte do seu ressentimento. Ao ver com sabedoria que tudo aquilo que é
insatisfatório é impermanente, você verá a conexão entre o desejo e o
insatisfatório. Como você está apegado a um objeto impermanente, você ficará
desapontado com as mudanças que ocorrem nele. Ao ver com sabedoria, verá que
aquilo que é impermanente e insatisfatório não possui um eu.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então você poderá pensar
“Ah! Como este objeto irá mudar, eu preciso ser rápido e esperto para tirar
vantagem deste objeto neste momento e obter minha satisfação tão rápido quanto
possível antes que ele desapareça. Amanhã ele não estará mais aqui.” Nesse caso
você deve se lembrar que a pressa é a inimiga da perfeição. Se você tomar
decisões apressadas e fizer algo tolo, irá se arrepender mais tarde. Algumas
vezes você se sente atraído por uma pessoa, por exemplo, e se empenha em
conquistá-la sem dar muita atenção ao fato de que mais tarde irá descobrir
muitos defeitos nela. Nas decisões apressadas não existe atenção plena. Você
não pode vencer o processo de mudança, nem pode pará-lo tomando decisões
apressadas e tolas.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando a sua atenção plena
está bem desenvolvida, então mesmo sob pressão você toma a decisão acertada. A
única coisa sensata a ser feita para vencer a impermanência é dar um passo
atrás e investigar a sua mente para ver se a decisão está sendo tomada com base
na sabedoria. Com a atenção plena você saberá como se beneficiar do momento
presente de forma a não se arrepender mais tarde. Qualquer decisão que você
tome com atenção plena fará com que você fique feliz e pacífico e nunca fará
com que você se arrependa mais tarde.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Lembre-se sempre que <i>a
atenção plena é o estado mental pleno de generosidade, amor bondade e sabedoria
juntamente com a compaixão, alegria altruísta e equanimidade. Sempre que você
estiver<span style="mso-spacerun: yes">  </span>prestando atenção em algo, você
deve se perguntar se a sua mente contém esses fatores. Se não contiver, você
não está com<span style="mso-spacerun: yes">  </span>atenção plena</i><i
style='mso-bidi-font-style:normal'>.<o:p></o:p></i></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Com generosidade na mente você
abandonará qualquer visão, som, aroma, sabor, tangível e pensamento agradável
sem hesitação.<span style="mso-spacerun: yes">  </span>Você, seguramente,
deverá reconhecê-los como atraentes no sentido convencional. Compreenda que é
devido à sua atratividade que as pessoas se apegam a eles e se envolvem com
eles. Quanto mais profundo for o envolvimento, mais profundo será o sofrimento.
Com amor bondade na mente você não tentará rejeitar nenhuma visão, som, aroma,
sabor, tangível e pensamento se eles não forem atraentes. Com<span
style="mso-spacerun: yes">  </span>atenção plena compreenda que eles são
impermanentes. Quando uma visão, som, aroma, sabor, tangível ou pensamento
parecer<span style="mso-spacerun: yes">  </span>identificado com o eu, olhe
para isso com sabedoria como um conceito irreal, noções errôneas inculcadas na
sua mente pelo condicionamento ao longo de muitas gerações.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Atenção
plena não é ter cuidado. Não é ser esperto. Qualquer pessoa pode ser cuidadosa
e esperta. Um equilibrista sobre o arame cem metros acima do solo é cuidadoso.
Igual aos acrobatas que realizam todo o tipo de façanhas. Os alpinistas que
escalam montanhas altíssimas com paredões inclinados, escorregadios, têm que
ser muito cuidadosos. Muitos ladrões são muito espertos e passam a perna na
polícia. Muitos traficantes de drogas, ladrões de bancos, criminosos são muito
espertos. Não se pode considerar que nenhum deles tenha atenção plena.<o:p></o:p></span></p>

<p class=Normal><i style='mso-bidi-font-style:normal'><span
lang=PT-BR style='mso-ansi-language:PT-BR'>Atenção plena é o estado da mente
que reflete sobre si mesma para não ser aprisionada pelo desejo, raiva e
ignorância que causam o sofrimento tanto para a própria pessoa como para os
outros ou ambos.<o:p></o:p></span></i></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando
dizemos para as pessoas que não cultivem o ressentimento, algumas pessoas nos
perguntam como podemos viver sem ressentimento? Esse é o milagre da atenção
plena. Ao praticar a atenção plena você aprende a fazer as coisas mais
difícieis com facilidade. Não ficar ressentido, desejoso ou confuso é muito <a
name=PVW>dificil</a>. Através do treinamento constante da atenção plena você
aprende a viver sem ressentimento, cobiça ou confusão. Por outro lado, estar
com a atenção plena é mais difícil do que estar sem a atenção plena. Mas você
aprende a fazer o que é<span style="mso-spacerun: yes">  </span>mais difícil
com facilidade, ao invés de fazer aquilo que é<span style="mso-spacerun: yes"> 
</span>mais fácil. Por essa razão o Buda disse:<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>Para o bom
fazer o bem é fácil<br>

Para o mau
fazer o mal é fácil<br>

Para o mau
fazer o bem é difícil<br>

Para o nobre
fazer o mal é difícil<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso significa que aquilo
que é muito difícil no começo fica mais fácil através da prática constante.<o:p></o:p></span></p>


