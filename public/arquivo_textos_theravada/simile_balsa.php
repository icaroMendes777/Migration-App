<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Qual a
utilidade de uma balsa? Usá-la para cruzar uma grande extensão d´água, o que
seria difícil sem ela. O exame detalhado da aplicação deste símile empregado
pelo Buda, no <i style='mso-bidi-font-style:normal'><a href="../sutta/MN22.php">Alagaddupama
Sutta</a>,</i> no Majjhima Nikaya, elucida a habilidade que ele teve em
escolhê-lo para ilustrar de forma precisa o que as pessoas, que não
compreendem<span style="mso-spacerun: yes">  </span>completamente o significado
das religiões, têm feito ao longo da história. Nesse símile o Buda indica que
se um homem, depois de cruzar uma grande extensão d´água sobre uma balsa,
estivesse determinado a carregá-la sobre os ombros, pensando que ao assim fazer
estaria demonstrando para a balsa a sua gratidão por tê-lo ajudado a salvar a
sua vida, ele estaria agindo como um tolo.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O símile do caçador de
serpentes empregado pelo Buda no mesmo Sutta, mencionado anteriormente, também
é indispensável para ilustrar o perigo de se compreender uma religião da forma
incorreta. Se uma pessoa que não sabe como pegar uma cobra venenosa tentasse
segurar a cobra pelo corpo ou pela cauda a pessoa poderia ser picada e como
conseqüência teria um grave ferimento ou poderia até morrer. A mensagem desses
dois símiles, uma vez compreendida plenamente, facilitaria a melhor compreensão
da tensão derivada do aumento da violência e criminalidade em nome da religião
na sociedade moderna.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A compreensão incorreta da
religião pode conduzir o homem a justificar a sua cobiça, raiva e tolice. As
suas idéias distorcidas, percepções distorcidas e consciência distorcida fazem
com que ele entenda a religião de forma incorreta minando a própria
infra-estrutura dela, causando mais dor e sofrimento – exatamente como quando
ele agarra a cobra de forma incorreta.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A compreensão incorreta de
uma religião pode também ser o caminho direto para anular o propósito da
religião e encorajar as pessoas a cometerem atrocidades em nome da própria fé.
Algumas vezes as pessoas não só se apegam à religião mas, de forma ingênua
obedecem a qualquer um, homem ou mulher que, por ser um orador persuasivo, é
capaz de promover e justificar a violência e práticas não éticas em nome da
religião. Ao dar apoio para tais pessoas através de dedicação de tempo,
habilidade, ou recursos, elas apenas contribuirão para<span
style="mso-spacerun: yes">  </span>aumentar sua cobiça, raiva e ignorância. Cegas
devido às crenças religiosas, elas podem até mesmo tentar com zelo exagerado
proteger as suas religiões, não apenas inculcando o raiva e o medo em muitos
dos fiéis crédulos, mas também defendendo até mesmo o assassinato em nome das
suas crenças.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Se uma pessoa simplesmente
se agarrar à balsa depois de usá-la para cruzar a grande extensão d´água, ao
invés de deixá-la na margem para que outra pessoa a use, ela tampouco estará
agindo de forma sábia. Ao invés disso ela faz com que a balsa se torne um pesado
fardo sobre os seus ombros. A balsa foi feita com capim, gravetos, galhos e
folhas que foram amarrados com uma corda ou com a casca de uma árvore. Da mesma
forma este corpo é composto de forma, sensação, percepção, formação mental e
consciência que estão atados juntos pela ignorância e desejo, compondo o
complexo mentalidade-materialidade (nome e forma). Assim como aquela pessoa se agarra à balsa
composta de capim, gravetos, galhos e folhas nós nos agarramos ao corpo e mente
composto de forma, sensação, percepção, formação mental e consciência atadas
pela ignorância e desejo. A pessoa agarrada à balsa que a ajudou a cruzar a
grande extensão d´água continuará a carregar o fardo daquela mesma balsa. Do
mesmo modo, ao nos agarrarmos ao nosso complexo de mentalidade-materialidade (nome e forma) e
às nossas crenças religiosas continuamos a carregar o nosso fardo. Ela
permanece atada à balsa e nós à mente-corpo. Ela está na<span
style="mso-spacerun: yes">   </span>margem e nós estamos no Samsara. Este corpo
e mente, juntamente com as sensações, percepções e formações mentais, não existe
para que nos apeguemos a ele, mas para que obtenhamos o conhecimento e o
insight necessários para alcançar a libertação do Samsara. “Bhikkhus,” disse o
Buda, “vocês devem abandonar até mesmo os bons ensinamentos, mais ainda os
falsos.” Os bons ensinamentos nos beneficiam apenas se os usamos. Igual à
balsa. Nenhum ensinamento, não importa quão bom seja, poderá nos ajudar se nós
simplesmente nos apegarmos a ele. O apego até mesmo ao bom ensinamento pode
causar dor e sofrimento. Imagine quanto mais doloroso pode ser o apego a coisas
ruins! A pessoa que emprega a balsa para cruzar a grande extensão d´água tem
que ser sábia. A pessoa que emprega este complexo mente-corpo para cruzar o
oceano do Samsara também tem que ser sábia. Portanto, ela não irá se apegar de
nenhuma forma a este complexo mente-corpo. Se ela assim o fizer não será capaz
de alcançar a Libertação.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O apego a idéias, sem a
prática, também pode com facilidade converter as pessoas em fanáticos
religiosos que irão buscar refúgio na violência para solucionar problemas, pois
elas desconhecem completamente aquilo que a sua religião lhes ensina. As
pessoas não familiarizadas com a mensagem da sua religião vivem constantemente
atemorizadas pelas críticas dirigidas a ela, e desejando protegê-la querem destruir
as pessoas que possuam crenças diferentes. O temor da crítica surge na mente
mal direcionada pelo sistema de crença ambivalente que não é capaz de atestar
pela segurança e realidade. O Buda disse: “A sua mente mal direcionada pode
causar-lhes mais dano que todos os seus inimigos juntos no mundo . Da mesma
maneira, ele disse: “Uma mente guiada corretamente pode trazer-lhes mais
benefício do que os seus pais, amigos e parentes, todos juntos.” O verdadeiro
conquistador não é aquele que conquista milhares e milhares de pessoas em um
campo de batalha mas aquele que conquista a si mesmo. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Embora o Buda não tenha
sequer indiretamente causado dano a qualquer pessoa que seja, existem até mesmo
entre os Budistas aqueles que acreditam que devem proteger o seu país, matando
tantos quanto sejam necessários de forma a proteger o Budismo, a religião da
paz, harmonia, compaixão e amor bondade. Matar, ou mesmo o pensamento de matar
qualquer ser vivo, sem falar de seres humanos, é diametralmente oposto ao
ensinamento do Buda compassivo, que disse: “Ele é chamado sábio e virtuoso,
aquele que desejando o sucesso, não comete crimes em benefício próprio, em
benefício dos próprios filhos, em benefício de outros para obter riqueza, ou
mesmo pelo seu país.” Os ensinamentos do Buda estão acima de todas as noções de
país, cultura, linguagem, afiliações étnicas e todo o demais, pois ele apenas
ensinou a verdade que é permanente, eterna e que não está atada a nada no
mundo.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ao embarcar na balsa você
deve inspecioná-la com cuidado para verificar se ela é segura e construída da
forma correta para que você não se afogue pelo fato de usar uma balsa
defeituosa. Da mesma forma, você deve cuidadosamente aprender e examinar de
forma crítica qualquer religião antes de aceitá-la ou rejeitá-la. Ouvir com
paciência alguém que critique o Buda, Dhamma ou a Sangha é altamente
recomendado nos ensinamentos do Buda.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se alguém insulta, ofende,
xinga e molesta o Tathagata por isso, o Tathagata, por conta disso, não sente
nenhuma contrariedade, amargura ou tristeza no seu coração. E se alguém honra,
respeita, reverencia e venera o Tathagata por isso, o Tathagata, por conta
disso, não sente prazer, alegria ou exaltação no seu coração. Se alguém honra,
respeita, reverencia e venera o Tathagata por isso, o Tathagata, por conta
disso, pensa da seguinte forma: ‘Eles realizam esse tipo de cerimônias por
aquilo (o agregado mente-corpo) que já foi plenamente compreendido.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Portanto, bhikkhus, se
alguém os insultar, ofender, xingar e molestar, vocês não deverão, por conta
disso,<span style="mso-spacerun: yes">  </span>dar lugar a nenhuma
contrariedade, amargura ou tristeza no seu coração. E se alguém os honrar,
respeitar, reverenciar e venerar, vocês não deverão, por conta disso, dar lugar
a nenhum prazer, alegria ou exaltação no seu coração. Se alguém os honrar,
respeitar, reverenciar e venerar, por conta disso, vocês devem pensar da
seguinte forma: ‘Eles realizam esse tipo de cerimônias por aquilo (o agregado
mente-corpo) que já foi plenamente compreendido.’”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>No Budismo, a investigação
analítica e o conhecimento crítico do Dhamma são fatores essenciais para a
Libertação. Pois, se você tem certeza que aquilo que você pratica é verdadeiro
não deveria ficar alarmado com a crítica. Ao invés disso, você deveria<span
style="mso-spacerun: yes">  </span>receber com prazer a investigação crítica
pois assim você poderá olhar para a sua prática por ângulos distintos. Se você
sabe que um pedaço de ouro é ouro, por exemplo, você permitiria sem hesitação
que qualquer ourives treinado testasse o pedaço cortando, fundindo, esfregando
e martelando, pois você está seguro que ele não irá concluir que o seu ouro na
verdade é cobre. Somente se você lhe desse um pedaço de chumbo banhado de ouro
você teria motivo para temer o teste.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O Buda nos aconselhou a não
ficarmos alarmados pela crítica, mas a ouvir a crítica com cuidado e atenção
plena sem ficarmos perturbados por aquilo que estamos ouvindo e que na nossa
avaliação nos atenhamos ao que foi dito. Após uma investigação detalhada, com
certeza não encontraremos defeito no Buda, Dhamma ou Sangha. No entanto, descobriremos
que a crítica foi feita devido à raiva, preconceito, frustração, temor,
neurose, paranóia, etc. Então, é claro, ao invés de sentirmos raiva da pessoa
que tem todos esses problemas, deveríamos tentar ajudá-la com amor bondade. Ela
merece o nosso amor bondade e compaixão ao invés da raiva. Não existe raiva que
possa resolver ou que tenha resolvido qualquer tipo de problema no mundo, pois
neste mundo a raiva nunca é aplacada com a raiva, mas apenas com o amor.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Nos ensinamentos do Buda
não há espaço para resolver qualquer tipo de problema através de meios
violentos. Um Budista que esteja repleto de cobiça, raiva e delusão e desatento
à verdadeira mensagem do Buda, exercendo a sua completa liberdade de escolha e
responsabilidade asseguradas no Budismo, poderá matar alguém, mas ele nunca
será capaz de citar algum texto Budista que justifique e apóie o seu ato.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Supostamente devemos usar o
Dhamma do Buda sem nos apegarmos a ele, mas apenas para cruzar a torrente deste
ciclo de nascimento e morte – Samsara. Ele nos aconselhou a empregar o
ensinamento como se fosse uma balsa que é utilizada apenas para cruzar uma
extensão d´água e não para ser agarrada. É o apego apaixonado àquilo que
acreditamos, ao invés da compreensão de como devemos usá-lo para guiar a nossa vida
diária na direção correta, que estimula a raiva enraizada profundamente que
pode nos forçar a solucionar nossos problemas através de meios violentos. É o
apego apaixonado às coisas que cria todo tipo de problemas.<o:p></o:p></span></p>


