<p class=Tit1 align=center style='text-align:center'>Nyanaponika Thera</Tit1>

<p class=Normal>Nyanaponika Thera nasceu como Siegmund Feniger no dia 21 de Julho de 1901 em Hanau, Alemanha. Ele recebeu dos pais uma educação tradicional judaica e desde pequeno demonstrou um grande interessse pela religião. No final da adolescência ele começou a trabalhar com o pai no negócio de livros. Ao mesmo tempo sérias dúvidas religiosas o impulsionaram a uma intensa busca espiritual que acabou por levá-lo, através dos livros, a ter contato com o Budismo. A nova descoberta exerceu um apelo imediato que foi crescendo à medida que ele lia mais. Em 1922 ele se mudou com a família para Berlin onde teve contato com outros Budistas e obteve acesso a um conjunto muito mais amplo de ensinamentos sobre o Budismo. Foi também quando ele teve o primeiro contato com os escritos e traduções do <a href="nyanatiloka.php">Venerável Nyanatiloka</a> que haviam sido publicados na Alemanha. Siegmund também descobriu que o Ven. Nyanatiloka havia estabelecido um monastério para monges Ocidentais chamado Island Hermitage, em uma ilha num lago próximo a Dodanduwa, no Sri Lanka. Essa descoberta plantou uma idéia na sua mente que foi se tornando cada vez mais forte, de ir para a Ásia e tornar-se um monge. Em 1936 ele deixou a Alemanha e foi para o Sri Lanka sendo ordenado como monge pelo Ven. Nyanatiloka no Island Hermitage. Em 1939, com o início da guerra entre a Alemanha e a Inglaterra os dois monges alemães, tal como todos os demais residentes alemães das colonias britânicas, foram transferidos para campos de internamento, primeiro em Diyatalawa no Sri Lanka e posteriormente em Dehra Dun no norte da Índia. Apesar das precárias condições do internamento, durante esse periodo o Ven. Nyanaponika completou as traduções para o alemão do<i> Sutta Nipata,</i> o <i>Dhammasangani</i> (primeiro livro do<i> Abhidhamma Pitaka</i>) e o seu comentário, <i>Atthasalini</i>. Durante esse periodo ele também compilou uma antologia de textos sobre a meditação <i>Satipatthana</i>. Com o término da guerra ambos monges foram libertados e retornaram para o Sri Lanka em 1946.<o:p></o:p></span></p>

<p class=Normal>Em 1958 o Ven. Nyanaponika ajudou a fundar a Buddhist Publication Society, tendo servido como o seu primeiro editor e presidente até a sua aposentadoria em 1984. O Ven. Nyanaponika faleceu no Island Hermitage em 1994.<o:p></o:p></span></p>

<p class=Normal>Ele é autor, em inglês, dos livros <i>The Heart of Buddhist Meditation</i> (considerado um clássico sobre a meditação), <i>The Vision of Dhamma</i> e <i>Abhidhamma Studies</i>, todos eles publicados pela BPS.<o:p></o:p></span></p>

