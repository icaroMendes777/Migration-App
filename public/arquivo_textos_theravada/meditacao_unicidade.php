<p class=Normal>Nova Iorque, EUA (BBC News, 24 Abril 2011) - Num laboratório escondido, próximo a uma rua movimentada em Nova Iorque, um neurologista vem colocando monges Budistas Tibetanos numa máquina do tamanho de um carro, para escanear o seu cérebro de modo a melhor entender a antiga prática de meditação. 

<p class=Normal>Mas poderia essa pesquisa incomum não somente revelar os segredos de uma vida harmoniosa mas também ajudar a entender algumas das mais misteriosas doenças do mundo? </p>

<p class=Normal>Zoran Josipovic, um cientista pesquisador e professor adjunto na New York University, diz que ele tem observado os cérebros dos monges durante a meditação para entender como os cérebros se re-organizam durante esse exercício. </p>

<p class=Normal>Desde 2008, o pesquisador tem colocado as mentes e corpos de proeminentes figuras Budistas numa máquina de imagem por ressonância magnética funcional (fMRI) que pesa 5 toneladas. </p>

<p class=Normal>O escaneamento segue o fluxo sanguíneo no cérebro dos monges enquanto eles meditam dentro do confinamento da máquina, que produz um ritmo musical ao estar em operação. </p>

<p class=Normal>O Dr. Josipovic, que também faz um bico como monge Budista, diz que espera descobrir como alguns meditadores atingem um estado de "não-dualidade" ou "unicidade" com o mundo, uma consciência unificada entre a pessoa e o seu meio-ambiente. </p>

<p class=Normal>"Uma das coisas que a meditação oferece para aqueles que a praticam muito é o desenvolvimento da atenção," diz o Dr. Josipovic, adicionando que o aprimoramento dessa habilidade pode conduzir a um estado com mais tranqüilidade e felicidade. </p> 

<p class=Normal>"As pesquisas da meditação, particularmente nos últimos 10 anos, têm sido muito promissoras porque apontam para uma habilidade do cérebro de mudança e otimização de um modo que antes não se sabia ser possível." </p>

<p class=Normal>Ao relaxar no estado de unicidade, a rede de neurônios dos praticantes com experiência muda na medida em que a barreira psicológica com o meio ambiente é reduzida, diz o Dr. Josipovic. </p>

<p class=Normal>E essa reorganização no cérebro pode conduzir ao que alguns meditadores afirmam ser uma profunda harmonia com aquilo que os cerca. </p>

<p class=Normal><b>Mudando a atenção</b></p>

<p class=Normal>A pesquisa do Dr. Josipovic faz parte de um esforço mais amplo para compreender aquilo que os cientistas denominaram o circuito automático no cérebro.

<p class=Normal>Ele diz que o cérebro parece estar organizado em dois circuitos: o circuito extrínseco e o circuito intrínseco, ou automático. </p>

<p class=Normal>A parte extrínseca do cérebro se torna ativa quando os indivíduos estão focados em atividades externas, tal como praticar esportes ou colocando café numa xícara. </p>

<p class=Normal>O circuito automático está ativo quando as pessoas refletem sobre temas que envolvem elas mesmas e as suas emoções. </p>

<p class=Normal>Mas os circuitos estão ativos ao mesmo tempo apenas raramente. Igual a uma gangorra, quando um está ativado o outro é reduzido. </p>

<p class=Normal>Essa organização neural permite que os indivíduos se concentrem com mais facilidade num determinado momento numa determinada tarefa, sem ser absorvido por distrações como por exemplo sonhar acordado. </p>

<p class=Normal>"O que estamos tentando fazer é basicamente identificar as mudanças nos circuitos no cérebro na medida em que a pessoa muda essas formas de atenção," diz o Dr. Josipovic. </p>

<p class=Normal>O Dr. Josipovic descobriu que alguns monges Budistas e outros meditadores experientes têm habilidade em manter ambos circuitos neurais ativos ao mesmo tempo durante a meditação - isto é, eles encontraram uma forma de manter ambos extremos da gangorra no alto, ao mesmo tempo. </p>

<p class=Normal>E o Dr. Josipovic acredita que essa habilidade de manter ambos circuitos interno e externo ativos ao mesmo tempo pode fazer com que os monges experimentem uma sensação de unicidade harmoniosa com o meio ambiente. </p>

<p class=Normal><b>Introspecção</b></p>

<p class=Normal>Antes os cientistas acreditavam que o circuito introspectivo automático no cérebro estava ativo simplesmente quando a pessoa não tinha nenhuma tarefa na qual focar a sua atenção. </p>

<p class=Normal>Mas os pesquisadores descobriram na última década que essa parte do cérebro se enche de atividade quando a pessoa pensa no eu. </p>

<p class=Normal>O circuito automático veio à luz em 2001 quando o Dr. Marcus Raichle, um neurologista na Washington University School of Medicine no estado de Missouri, EUA, começou a escanear os cérebros de indivíduos que não haviam recebido nenhuma tarefa para ser realizada. </p>

<p class=Normal>Os participantes rapidamente se entediaram e o Dr. Raichle observou um segundo circuito, que antes não havia sido observado, muito ativo. Mas para o pesquisador não ficou claro porque essa atividade estava ocorrendo. </p>

<p class=Normal>Outros cientistas rapidamente sugeriram que os pacientes do Dr. Raichle poderiam na verdade estar pensando sobre si mesmos. </p>

<p class=Normal>Pouco tempo depois outros neurologistas, que conduziam estudos empregando filmes para estimular o cérebro, descobriram que quando havia uma trégua nas ações no filme, o circuito automático começava a brilhar - indicando que os participantes no estudo poderiam ter começado a pensar em si mesmos devido ao tédio. </p>

<p class=Normal>Mas o Dr. Raichle diz que o circuito automático é importante não somente para pensar na janta da noite passada. </p>

<p class=Normal>"Os pesquisadores têm se debatido com essa idéia de como sabemos que somos quem somos. O circuito automático tem algo da resposta sobre como isso pode ter surgido," ele diz. </p>

<p class=Normal>E o Dr. Raichle adiciona que aqueles que estudam o circuito automático também podem ajudar a desvendar os segredos que cercam alguns distúrbios psicológicos, como  a depressão, autismo e até mesmo Alzheimer. </p>

<p class=Normal>"Se você analisar a doença de Alzheimer e verificar se há alguma região em particular do cérebro afetada, o que é surpreendente, é que na verdade a doença afeta o circuito automático," diz o Dr. Raichle, adicionando que a pesquisa do circuito intrínseco, tal como a feita pelo Dr. Josipovic, pode ajudar a explicar porque acontece isso. </p>

<p class=Normal>Cindy Lustig, professora assistente de psicologia e neurologia na Universidade de Michigan concorda. </p>

<p class=Normal>"É um circuito importante mas pouco estudado no cérebro que parece estar muito envolvido em muitos distúrbios neurológicos, incluindo o autismo e Alzheimer, e é importante compreender como esse circuito interage com o circuito orientado para tarefas (extrínseco)," ela diz. "É como se fosse a outra peça do quebra-cabeças que foi ignorada durante muito tempo." </p>

<p class=Normal>O Dr. Josipovic escaneou os cérebros de mais de 20 meditadores experientes, ambos monges e monjas, que estudam o estilo de meditação do Budismo Tibetano, para melhor entender esse circuito misterioso. </p>

<p class=Normal>Ele diz que a sua pesquisa, que será publicada em breve, irá por enquanto continuar se concentrando na explicação das implicações neurológicas da unicidade e da tranqüilidade - embora a melhora no entendimento, como parte do processo, do autismo e Alzheimer, seria um bônus significativo. </p>


