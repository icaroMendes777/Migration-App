<p class=Normal>A recente onda de interesse
acerca do Budismo, tanto no Oriente como no Ocidente, tem sido marcada por uma
orientação vigorosa em direção à prática e pelo interesse em descobrir a paz e
a liberdade às quais a prática do Dhamma conduz. O zelo pela prática no entanto
tem sido freqüentemente acompanhado por um outro traço que pode não ser tão
benéfico, especificamente uma tendência de negligenciar ou mesmo menosprezar o
estudo metódico dos ensinamentos do Buda. Os argumentos que são usados em
defesa dessa atitude já nos são familiares. É dito por exemplo, que o estudo se
preocupa com palavras e conceitos, não com a realidade; que ele conduz somente
ao aprendizado, não à sabedoria, que pode somente modificar as nossas idéias
mas que não consegue tocar os níveis mais profundos das nossas vidas. Como
argumento decisivo o testemunho do próprio Buda é apresentado, com o seu famoso
comentário de que muito estudo sem a prática é o mesmo que contar as vacas que
pertencem a outras pessoas ou carregar uma balsa sobre a cabeça ao invés de
utilizá-la para cruzar um rio.<o:p></o:p></span></p>

<p class=Normal>Essa alegação, para
estarmos seguros, tem seu lado de verdade. Porém, ela também sofre de um
maniqueísmo que na verdade pode atrapalhar ao invés de ajudar o progresso no
caminho Budista. É certamente verdade que aprender sem praticar é infrutífero
mas o outro lado da moeda também deve ser tomado em conta. Deveria uma pessoa
arrebanhar vacas se ela desconhece como cuidar delas? Ela deveria tentar cruzar
um rio perigoso e violento sem saber como operar uma balsa? O próprio Buda
insistia em que os seus discípulos aprendessem e transmitissem o Dhamma tanto
na sua forma como na sua essência, mas ao invés de apelar para formulações
tradicionalistas, vamos investigar o valor e a utilidade do estudo do Dhamma. <o:p></o:p></span></p>

<p class=Normal>O ponto em questão, que
deve ser enfatizado, não é o estudo como uma disciplina acadêmica ou a
acumulação de um patrimônio de conhecimentos, mas a aquisição de um
conhecimento prático, profundo e sólido das doutrinas básicas do Budismo.
Agora, para enxergar porque isso é tão fundamental, devemos relembrar que toda
a prática do caminho Budista correto se desenvolve através do ato pelo qual
entramos no caminho - a tomada do refúgio na Jóia Tríplice. Se damos esse passo
honestamente, com a motivação correta, isso implica que reconhecemos nossa
necessidade de orientação espiritual e confiamos no Buda como nosso guia e no
seu ensinamento o veículo guia. Tomando refúgio no Dhamma não aceitamos
meramente uma técnica de meditação que podemos usar a nosso critério para
satisfazer nossos propósitos mas, um ensinamento profundo e abrangente acerca
da natureza da condição humana, um ensinamento desenhado para despertar em nós
a percepção dessa verdade e os meios para alcançar o completo e definitivo fim
do sofrimento. A libertação oferecida pelo Dhamma vem não simplesmente da
prática de meditação no contexto das nossas próprias pré-concepções e desejos
mas através da prática apoiada no fundamento do entendimento correto e pensamento
correto da forma como nos foram comunicados pelo Buda. <o:p></o:p></span></p>

<p class=Normal>Esse caráter cognitivo do
caminho Budista eleva o estudo da doutrina e a investigação intelectual a uma
posição de grande importância. Apesar de que o conhecimento que liberta a mente
da escravidão surge somente através do insight intuitivo e não de uma massa de
fatos doutrinários, o insight genuíno sempre se desenvolve sobre uma base de
compreensão conceitual preliminar dos princípios básicos essenciais ao
entendimento correto, na ausência do qual o seu desenvolvimento será
inevitavelmente obstruído. O estudo e reflexão sistemática através dos quais
alcançamos esse entendimento correto preparatório necessariamente envolve
conceitos e idéias. Mas antes que nos apressemos a dispensar o estudo do Dhamma
como sendo somente um emaranhado de verbosidade inútil, consideremos que
conceitos e idéias são as nossas ferramentas indispensáveis para a compreensão
e a comunicação. Conceitos no entanto podem ser ferramentas válidas ou
inválidas de compreensão; idéias podem ser frutíferas ou inúteis, capazes de
trazer um benefício imenso ou de resultar em dano enorme. O propósito de
estudar o Dhamma como parte da nossa busca espiritual é de aprender a
compreender a nossa experiência corretamente: de ser capaz de distinguir o
válido do inválido, o verdadeiro do falso, o benéfico do que não é benéfico. <o:p></o:p></span></p>

<p class=Normal>É somente fazendo uma
investigação completa e cuidadosa que estaremos na posição de rejeitar o que é
prejudicial ao nosso desenvolvimento e de nos dedicarmos com confiança ao
cultivo de tudo que é verdadeiramente benéfico. Sem ter alcançado esse
esclarecimento conceitual preliminar, sem ter sido bem sucedido no
&quot;aclarar nossas idéias&quot;, pode na verdade haver a prática séria das
técnicas de meditação Budista mas não haverá a prática da meditação que é parte
integral do Nobre Caminho Óctuplo. E embora essa meditação livre possa trazer
aos seus praticantes os benefícios mundanos de maior calma, atenção e
equanimidade, sem a direção do entendimento correto e da força impulsora do pensamento
correto, é questionável se ela irá conduzir à realização penetrante do Dhamma
ou ao seu objetivo final, a completa cessação do sofrimento. <o:p></o:p></span></p>

<p class=Normal>É quase impossível dar um
palavra que seja de aconselhamento, acerca do tema de estudo, que se aplique a
todos os seguidores do Dhamma. As necessidades e os interesses variam tanto, de
uma pessoa para outra, que cada uma terá que encontrar o equilíbrio entre o
estudo e a prática que melhor sirva ao seu caráter. Mas, sem hesitação pode ser
dito que todos aqueles que se esforçam seriamente para viver de acordo com os
ensinamentos Budistas irão notar que a sua prática será fortalecida pelo estudo
metódico do Dhamma. Tal tarefa, é claro, não será fácil, mas é justamente
enfrentando e superando os desafios que encontramos que o nosso entendimento
irá amadurecer e se desenvolver na sabedoria superior. <o:p></o:p></span></p>


