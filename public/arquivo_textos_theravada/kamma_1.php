<p class=Normal><b><a name=T1></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.
Compreendendo a Lei de Kamma</b><o:p></o:p></span></h3>

<p class=Normal><b><a name=T11></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>Kamma como
uma lei da natureza</b><o:p></o:p></span></h3>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O Budismo
ensina que todas as coisas, tanto materiais como imateriais, estão totalmente
sujeitas à influência de causas e são interdependentes. Esse fluxo natural das
coisas é chamado em termos usuais de “lei da natureza” e em Pali <i>niyama</i>
que no sentido literal quer dizer “certeza” ou “modo fixo,” referindo-se ao
fato de que condições específicas inevitavelmente levam a resultados
correspondentes.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>As leis da
natureza, embora baseadas de modo uniforme no princípio da dependência causal,
podem no entanto ser classificadas de acordo com os diferentes tipos de
relação. Os comentários Budistas descrevem cinco categorias de leis da natureza
ou <i>niyama</i>. Elas são:<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. <b><i>Utuniyama</i></b>:
a lei da natureza que diz respeito aos objetos físicos e mudanças no ambiente
natural, tais como o clima, a forma como as flores desabrocham durante o dia e
se fecham à noite, a forma como o solo, água e nutrientes ajudam uma árvore a
crescer e a forma como as coisas se desintegram e se decompõem. Esta perspectiva
enfatiza as mudanças ocasionadas pelo calor ou temperatura.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. <b><i>Bijaniyama</i></b>:
a lei da natureza que diz respeito à hereditariedade que é melhor descrita com
o ditado popular, “como a semente, assim a fruta.”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. <b><i>Cittaniyama</i></b>:
a lei da natureza que diz respeito aos processos mentais, o processo de
reconhecimento dos objetos dos sentidos e as reações mentais associadas a eles.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. <b><i>Kammaniyama</i></b>:
a lei da natureza que diz respeito ao comportamento humano, o processo de
geração das ações e os seus resultados. Em essência, isto é resumido nas
palavras, “Boas ações trazem bons resultados, más ações trazem maus
resultados.”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. <b><i>Dhammaniyama</i></b>:
a lei da natureza que governa a relação e interdependência de todas as coisas:
a forma como as coisas surgem, existem e depois cessam. Todas os fenômenos
estão sujeitos à mudança, estão num estado de aflição e são não-eu: essa é a
Lei. <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os quatro
primeiros <i>niyama</i> estão contidos ou estão baseados no quinto, <i>Dhammaniyama</i>,
a Lei do Dhamma ou Lei da Natureza. Poderia ser questionada a razão de <i>Dhammaniyama</i>,
pelo fato de ser a totalidade e também estar incluído como uma das subdivisões.
Isso se deve por que as quatro primeiras categorias não abrangem a extensão
completa de <i>Dhammaniyama</i>. <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Para ilustrar:
a população da Tailândia pode ser agrupada em diferentes categorias tais como a
realeza, o governo, os funcionários públicos, os comerciantes e o povo; ou ela
pode ser categorizada como a polícia, os militares, os funcionários públicos,
os estudantes e o povo; ou ela pode ser classificada em uma série de outras
formas. Na verdade o termo “povo” inclui todas as categorias do país. Os
funcionários públicos, os chefes de família, a polícia, os militares, os
estudantes e os comerciantes são todos igualmente membros do povo, mas eles são
selecionados porque cada um desses grupos possui as suas próprias
características distintas. Aquelas pessoas que não possuem uma característica
relevante em particular são agrupadas sob o título genérico, “o povo.” Além
disso, embora esses agrupamentos possam mudar de acordo com o seu propósito
particular, eles sempre estarão incluídos como parte do “povo” ou “multidão” ou
algum outro termo genérico semelhante. A inclusão de <i>Dhammaniyama</i> nos
cinco <i>niyama</i> deve ser compreendida desta forma.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Se estas
cinco leis da natureza são completas e abrangentes ou não, isto não é
importante. Os comentadores (das escrituras em Pali) detalharam esses cinco
grupos pois esta divisão se adequava<span style="mso-spacerun: yes">  </span>às
necessidades deles e qualquer outro grupo poderia ser incluído sob o quinto, <i>Dhammaniyama</i>,
da forma como foi mostrado no exemplo acima. O ponto importante que devemos ter
em mente é o propósito dos comentadores ao indicar esses cinco <i>niyama</i>.
Com respeito a isso, três pontos podem ser mencionados:<o:p></o:p></span></p>


<p class=Normal><i><span lang=PT-BR style='mso-ansi-language:PT-BR'>Primeiro</span></i><span
lang=PT-BR style='mso-ansi-language:PT-BR'>, este ensinamento enfatiza a
perspectiva Budista que vê o fluxo das coisas como estando sujeito a causas e
condições. Não importa quão em detalhe esta lei seja analisada, o que
encontraremos será apenas o funcionamento dessa Lei ou do estado de
interdependência. A compreensão dessa verdade nos permite aprender, viver e
praticar com um entendimento claro e firme da forma como as coisas são.
Eliminando, de forma definitiva, o problema de tentar responder a questões
sobre um Deus Criador com o poder de desviar o fluxo da Lei ( a não ser que
esse Deus se torne um dos fatores determinantes dentro do fluxo). Quando
provocados com questões equivocadas como, “Sem um ser para criar essas leis,
como elas podem ter surgido?” precisamos refletir que se deixadas por sua
própria conta, todas as coisas irão funcionar de uma forma ou de outra e é
assim como elas funcionam. É impossível que seja de qualquer outra forma. Os
seres humanos, observando e estudando esse estado de coisas, passam a chamá-lo
de “lei.” Mas quer seja chamado de lei ou não, isso não muda a sua forma de
operação.<o:p></o:p></span></p>


<p class=Normal><i><span lang=PT-BR style='mso-ansi-language:PT-BR'>Segundo</span></i><span
lang=PT-BR style='mso-ansi-language:PT-BR'>, ao analisarmos eventos, não
devemos reduzí-los completamente a leis únicas. Na verdade, um evento na
natureza pode surgir de qualquer uma dessas leis ou de uma combinação delas.
Por exemplo, o desabrochar de uma flor de lótus durante o dia e o seu
fechamento à noite não está sujeito apenas aos efeitos de <i>utuniyama</i>
(leis físicas), mas também aos de <i>bijaniyama</i> (hereditariedade). Quando
uma pessoa chora pode ser em grande parte devido aos efeitos de <i>cittaniyama</i>,
por conta dos estados mentais tristes ou felizes, ou pode ser a operação de <i>utuniyama</i>,
por conta de fumaça nos olhos.<o:p></o:p></span></p>


<p class=Normal><i><span lang=PT-BR style='mso-ansi-language:PT-BR'>Terceiro</span></i><span
lang=PT-BR style='mso-ansi-language:PT-BR'>, e mais importante, os comentadores
estão nos mostrando que a lei de kamma é apenas uma entre um grande número de
leis da natureza. O fato de ser mostrada como apenas uma entre cinco leis
diferentes nos lembra que nem todos os eventos são operações de kamma. Podemos
dizer que o kamma é aquela força que dirige a sociedade humana ou decide os valores
que ela tem. Embora seja apenas um tipo de lei da natureza, ela é a mais
importante para nós como seres humanos, porque ela é nossa responsabilidade.
Nós somos os criadores de kamma e kamma por seu lado molda o destino e as
condições das nossas vidas.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A maioria
das pessoas tende a perceber o mundo como estando parte sob o controle da
natureza e parte sob o controle dos seres humanos. Neste modelo, <i>kammaniyama</i>
é responsabilidade dos seres humanos, enquanto que os outros <i>niyama</i> são
do domínio da natureza.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>No processo
de <i>kammaniyama</i>, o fator da intenção ou volição é crucial. Portanto, a
lei de kamma é a lei que governa as operações da volição ou o mundo das ações e
atividades mentais intencionais das pessoas. Quer lidemos ou não com os outros <i>niyama</i>,
precisamos lidar com a lei de kamma e o nosso relacionamento com os outros <i>niyama</i>
serão influenciados por kamma de forma inevitável. Assim a lei de kamma é de
importância crucial ao regular o grau em que seremos capazes de criar e
controlar as coisas ao nosso redor.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Falando
corretamente, podemos dizer que a capacidade humana de entrar e se tornar um
fator dentro do processo natural de causa e efeito, que por seu lado faz surgir
a impressão de que somos capazes de controlar e manipular a natureza, se deve totalmente
a esta lei de kamma. Nas áreas científica e tecnológica, por exemplo,
interagimos com outros <i>niyama</i>, ou leis da natureza, estudando as suas
verdades e exercendo influência sobre elas de acordo com a sua natureza,
criando a impressão de que somos capazes de manipular e controlar o mundo
natural.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Além disso,
a nossa volição ou intenção molda os nossos relacionamentos pessoais e sociais,
bem como nossa interação com as outras coisas no ambiente à nossa volta.
Através da volição moldamos nossa própria identidade e estilo de vida, posição
social e destino. Pelo fato da lei de kamma governar todo nosso universo
volitivo e criativo é que os ensinamentos do Buda enfatizam muito a sua
importância com a frase:<span style="mso-spacerun: yes">  </span><i>Kammuna
vattati loko</i>: "As ações é que
fazem o mundo girar" (o mundo é dirigido por kamma). [<a href="../sutta/MN98.php#R6">MN98</a>]<o:p></o:p></span></p>


<p class=Normal><b><a name=T12></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>A lei de
kamma e a preferência social</b><o:p></o:p></span></h3>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Além dos
cinco tipos de leis da natureza mencionados acima, existe um outro tipo de lei
que é específico dos seres humanos e não está diretamente relacionado com a
natureza. Trata-se de códigos de leis fixados e acordados pela sociedade,
consistindo em decretos sociais, costumes e leis. Eles poderiam ser colocados
no final da lista acima como um sexto tipo de lei, mas eles não possuem um nome
em Pali. Vamos chamá-los de Preferência Social. [<a href="#N2">2</a>] <a
name=R2></a>Esses códigos de leis sociais são o produto do pensamento humano e
como tal estão relacionados com a lei de kamma. Eles não são, no entanto, a lei
de kamma como tal. Eles apenas lhe servem de complemento e não possuem a mesma
relação com a verdade da natureza como ocorre com a lei de kamma, como será
mostrado a seguir. No entanto, porque existe uma relação com a lei de kamma
eles tendem a ser confundidos com ela e os mal entendidos surgem com freqüência.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Como ambos,
<i>kammaniyama</i> e a Preferência Social, são preocupações dos seres humanos e
estão intimamente relacionados com a vida humana, é muito importante que as
diferenças entre ambos seja compreendida com clareza.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>De forma
geral podemos afirmar que a lei de kamma é a lei da natureza que lida com as
ações humanas, enquanto que a Preferência Social ou leis sociais fazem parte do
processo de criação humana, relacionadas com a natureza à medida que são um
produto do processo natural do pensamento humano. Em essência, com a lei de
kamma os seres humanos recebem os frutos das suas ações de acordo com o
processo da natureza, enquanto que nas leis sociais, os seres humanos assumem
responsabilidade pelas suas ações através de um processo estabelecido por eles
mesmos.<o:p></o:p></span></p>


<p class=Normal><b><a name=T13></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>O
significado de kamma</b><o:p></o:p></span></h3>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>De um ponto
de vista etimológico, kamma significa “trabalho” ou “ação.” Mas no contexto dos
ensinamentos do Buda é definido de forma mais específica como “ação baseada na intenção”
ou “atos cometidos de forma deliberada.” Ações desprovidas de intenção não são
consideradas como kamma nos ensinamentos do Buda.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Esta
definição, no entanto, é muito geral. Se quisermos esclarecer a abrangência
completa do significado de kamma, precisamos analisá-lo em mais detalhe,
dividindo-o em diferentes perspectivas ou níveis, desta forma:<o:p></o:p></span></p>


<p class=Normal><b><a name=T13a></a><b><span lang=PT-BR style='mso-ansi-language:
PT-BR'>a. Kamma como intenção</b><o:p></o:p></span></b></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
essência, kamma é intenção (<i>cetana</i>) e esta palavra inclui volição,
escolha e decisão, o ímpeto mental que conduz à ação. A intenção é aquilo que
incita e dirige todas as ações humanas, ambas, criativas e destrutivas e por
isso é a essência de kamma, como enunciado pelo Buda, <i>Cetanaham bhikkhave
kammam vadami</i>: Bhikkhus! Intenção, eu digo, é kamma. Pela intenção, 
a pessoa faz kamma através do corpo, linguagem, e mente.[AN III.415] (<a href="../sutta/ANVI.63.php">AN VI.63</a>)<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Neste ponto
podemos gastar um pouco mais de tempo para ampliar a nossa compreensão dessa
palavra “intenção.” Intenção no contexto Budista tem um significado muito mais
sutil do que o uso mais geral dessa palavra. Em geral tendemos a usá-la quando
queremos proporcionar um elo de ligação entre o pensamento interno e as suas
ações externas resultantes. Por exemplo, podemos dizer, “Eu não tinha intenção
de fazer isso,” “Eu não tinha intenção de dizer isso” ou “ela fez isso de forma
intencional.”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Mas, de
acordo com os ensinamentos Budistas, todas as ações e linguagem, todos os
pensamentos, não importa quão fugazes sejam, e as respostas da mente a
sensações recebidas através do olho, ouvido, nariz, língua, corpo e mente, sem
exceção, contêm elementos de intenção. Assim, a intenção é a escolha volitiva
feita pela mente em relação aos objetos para os quais a atenção é dirigida; é o
fator que conduz a mente a se inclinar ou a repelir os vários objetos da
atenção, ou de prosseguir em uma certa direção; é o que guia ou governa como a
mente responde aos estímulos; é a força que planeja e organiza os movimentos da
mente e no final das contas é aquilo que determina os estados experimentados
pela mente.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Uma
ocorrência de intenção é uma ocorrência de kamma. Quando há kamma existe
resultado direto. Até mesmo um pensamento fugaz, embora não seja
particularmente importante, não será no entanto desprovido de conseqüência.
Será no mínimo uma “pequena nódoa” de kamma, adicionada ao fluxo de condições
que moldam a atividade mental. Com a prática repetida, através da proliferação
mental ou expressão por meio de ações externas, o resultado se tornará mais
marcado sob a forma de traços de caráter, características físicas ou repercussões
de fontes externas.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Uma
intenção destrutiva não precisa ocorrer num nível grosseiro. Ela pode, por
exemplo, levar à destruição algo muito pequeno, tal como quando rasgamos uma
folha de papel com raiva . Muito embora o pedaço de papel em si não tenha importância,
mesmo assim a ação tem algum efeito sobre a qualidade da mente. O efeito é bem
distinto ao rasgarmos a folha de papel com um estado mental neutro, como ao
jogar fora um papel de rascunho. Se existe execução repetida desse tipo de
intenção enraivecida, os efeitos da acumulação irão se tornar cada vez mais
claros e podem se desenvolver para níveis mais substanciais.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Considere
as partículas de pó que entram flutuando desapercebidas num cômodo; não existe
nenhuma partícula que esteja desprovida de conseqüência. É o mesmo com relação
à mente. Mas o peso daquela conseqüência, além de depender da quantidade de
“poeira” mental, também está relacionado com a qualidade da mente. Por exemplo,
partículas de pó que pousem sobre a superfície de uma rua têm que ser em grande
número até que a rua pareça estar suja. Partículas de pó que pousem no chão,
embora numa quantidade muito menor poderão fazer com que o piso pareça mais
sujo que a rua. Uma quantidade ainda menor de pó que se acumule sobre uma mesa
fará com que ela pareça estar suficientemente suja para causar irritação. Uma
quantidade ainda menor que pouse sobre um espelho irá fazer com que este pareça
sujo e interferirá com a sua função. Uma pequena partícula de pó sobre as
lentes de um óculos pode ser notada e pode prejudicar a visão. Da mesma forma,
volição ou intenção, não importa quão pequena, não está desprovida de fruto.
Como disse o Buda:<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>“Todo kamma, quer seja bom ou mal, produz frutos. Não existe kamma, não
importa quão pequeno, que seja desprovido de fruto.” [Jataka IV.390]<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>De qualquer
forma, os resultados mentais da lei de kamma em geral são negligenciados, então
uma outra ilustração poderá ajudar:<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Existem
muitas variedades de água: água de esgoto, água de um canal, água da torneira e
água destilada para preparar uma injeção hipodérmica. A água de esgoto é um
hábitat aceitável para muitos tipos de animais aquáticos mas não é adequada
para banho, para beber ou fins medicinais. A água de um canal pode ser usada
para banho ou lavar roupas, mas não é potável. A água da torneira é potável mas
não deve ser usada para preparar uma injeção hipodérmica. Se não há uma
necessidade especial então a água da torneira servirá na maioria dos casos, mas
seria imprudente usá-la para preparar uma injeção hipodérmica.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Da mesma
forma, a mente possui níveis variados de refinamento ou nitidez, dependendo do
kamma acumulado. Enquanto a mente estiver sendo usada num nível mais grosseiro,
não haverá problema aparente, mas se for necessário usá-la num nível mais refinado,
o kamma inábil existente, mesmo numa escala mínima, pode se tornar um
obstáculo.<o:p></o:p></span></p>


<p class=Normal><b><a name=T13b></a><b><span lang=PT-BR style='mso-ansi-language:
PT-BR'>b. Kamma como fator condicionante</b><o:p></o:p></span></b></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Expandindo
nossa perspectiva, encontramos kamma como um componente dentro de todo processo
vital, sendo o agente que molda a direção da vida. Isto é kamma com o sentido
de “<i>sankhara</i>,” [<a href="#N3">3</a>] <a name=R3></a>como aparece no
Ciclo da Origem Dependente [<a href="#N4">4</a>], <a name=R4></a>onde é
descrito como o “agente que molda a mente.” Isto se refere aos fatores ou
qualidades da mente que, com a intenção no comando, moldam a mente em estados
bons, ruins ou neutros, que por sua vez moldam o processo do pensamento e os
seus efeitos através do corpo e linguagem. Nesse contexto, kamma pode ser definido
simplesmente como impulsos volitivos. Mesmo nessa definição tomamos a intenção
como sendo a essência e é por isso que algumas vezes vemos a palavra <i>sankhara</i>
traduzida simplesmente como intenção.<o:p></o:p></span></p>


<p class=Normal><b><a name=T13c></a><b><span lang=PT-BR style='mso-ansi-language:
PT-BR'>c. Kamma como responsabilidade pessoal</b><o:p></o:p></span></b></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Olhemos
agora um pouco mais para o exterior, para o nível do relacionamento do
indivíduo com o mundo. Kamma neste sentido se refere à expressão dos
pensamentos através da linguagem e ações. Isto é, o comportamento sob uma
perspectiva ética, quer seja num nível estreito, imediato ou num nível mais
amplo, incluindo o passado e o futuro. Kamma neste sentido corresponde ao
significado amplo e geral dado acima. Esse é o significado de kamma que com
mais freqüência é encontrado nas escrituras, onde ele ocorre como incentivo para
encorajar ações boas e responsáveis, tal como nas palavras do Buda:<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'>“Bhikkhus! Essas duas coisas são causa para o remorso. Quais duas?
Algumas pessoas neste mundo não realizaram bom kamma, não foram hábeis, não
realizaram méritos como salvaguarda contra o temor. Elas apenas realizaram
kamma ruim, apenas kamma grosseiro, apenas kamma prejudicial. Como conseqüência
elas experimentam o remorso, pensando, ‘Eu não realizei bom kamma, Eu apenas
realizei kamma ruim.’” [<a href="../sutta/It.30.php">Itivuttaka 30</a>]<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Vale a pena
observar que na atualidade kamma não só é ensinado quase que exclusivamente sob
esta perspectiva, mas também é amplamente tratado sob a perspectiva de vidas
passadas.<o:p></o:p></span></p>


<p class=Normal><b><a name=T13d></a><b><span lang=PT-BR style='mso-ansi-language:
PT-BR'>d. Kamma como atividade social ou profissional</b><o:p></o:p></span></b></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Sob um raio
ainda mais amplo, isto é, sob a perspectiva da atividade social, temos kamma
com o sentido de trabalho, labor ou profissão. Isto se refere aos estilos de
vida e ações sociais que resultam da intenção, que por sua vez afetam a
sociedade. Como mencionado no Vasettha Sutta:<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><p class=Normal style='margin-left:.75in'> "Uma pessoa não é brâmane pelo nascimento, nem pelo nascimento ela não é brâmane. Através da ação ela é brâmane. Através da ação ela não é brâmane.</p>


<p class=Normal style='margin-left:.75in'>"Pois os homens são agricultores através das suas ações, e através das suas ações eles também são trabalhadores; e os homens são comerciantes através das suas ações, e através das suas ações eles também são criados.</p>


<p class=Normal style='margin-left:.75in'>"E os homens são ladrões através das suas ações, e através das suas ações eles também são soldados; e os homens são brâmanes através
das suas ações, e através das suas ações eles também são governantes.</p>


<p class=Normal style='margin-left:.75in'>“Portanto, é assim como aqueles verdadeiramente sábios vêm as ações como elas na verdade
são, enxergando a origem dependente, com habilidade nas ações e nos seus resultados.</p>


<p class=Normal style='margin-left:.75in'>"As ações é que
fazem o mundo girar, as ações fazem esta geração girar. Os seres vivos estão atados pelas
ações, da mesma forma que a roda da
carruagem pelo eixo. [<a href="../sutta/MN98.php#T55">MN98</a>]</p>


<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-language:PT-BR'>* * *<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Após
analisar esses quatro matizes distintos do significado da palavra “kamma,”
convém mais uma vez enfatizar que qualquer definição de kamma tem que ser
sempre baseada na intenção. A intenção é o fator que guia o nosso
relacionamento com as outras coisas. Quer ajamos sob a influência das
tendências inábeis, sob a forma de cobiça, raiva e delusão ou tendências
hábeis, tudo está sob o controle da intenção.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Qualquer
ação desprovida de intenção não tem impacto na lei de kamma. Isto é, não faz
parte do escopo da lei de kamma, mas de algum outro <i>niyama</i>, tal como <i>utuniyama</i>
(leis físicas). Essas ações possuem a mesma importância que um barranco desmoronando,
uma pedra caindo de uma montanha ou um galho morto caindo de uma árvore.<o:p></o:p></span></p>


<p class=Normal><b><a name=T14></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>Tipos de
Kamma</b><o:p></o:p></span></h3>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>No que se
refere às suas qualidades ou raízes, kamma pode ser dividido em dois tipos
principais. Eles são:<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. <b><i>Akusala
kamma</i></b>: kamma que é inábil, ações que não são benéficas ou que são prejudiciais;
em especial ações que nascem de <i>akusala mula</i>, as raízes da inabilidade,
que são a cobiça, a raiva e a delusão.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. <b><i>Kusala
kamma</i></b>: ações que são hábeis ou benéficas; em especial ações que nascem
das três <i>kusala mula</i> ou raízes da habilidade, que são a não-cobiça, a
não-raiva e a não-delusão.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Como
alternativa, kamma pode ser classificado de acordo com os caminhos ou canais
através dos quais ele ocorre, sendo que estes são três:<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>1. <b>Kamma corporal</b>: ações intencionais com o corpo.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2. <b>Kamma verbal</b>: ações intencionais com a linguagem.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>3. <b>Kamma mental</b>: ações intencionais com a mente.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Integrando
ambas classificações descritas acima, temos no total seis tipos de kamma: kamma
que é inábil corporal, verbal e mental; e kamma que é hábil corporal, verbal e
mental.&nbsp;<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Uma outra
forma de classificar kamma é de acordo como os seus resultados. Nesta
classificação existem quatro categorias:<o:p></o:p></span></p>


<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Kamma
escuro com um resultado sombrio: </b></span></b><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Isto se refere a ações corporais, ações verbais e ações mentais que são
prejudiciais. Exemplos simples são matar, roubar, comportamento sexual
impróprio, mentir e tomar substâncias embriagantes. [<a href="#N5">5</a>]<o:p></o:p></span></p>


<p class=Normal><b><a name=R5></a><b><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2. Kamma claro com um resultado luminoso: </b></span></b><span lang=PT-BR
style='mso-ansi-language:PT-BR'>Essas são ações corporais, verbais e mentais que
são benéficas, tal como agir de acordo com as dez bases para a conduta hábil.[<a
href="#N6">6</a>] <o:p></o:p></span></p>


<p class=Normal><b><a name=R6></a><b><span lang=PT-BR style='mso-ansi-language:
PT-BR'>3. Kamma escuro e claro com um resultado sombrio e luminoso:</b> </span></b><span
lang=PT-BR style='mso-ansi-language:PT-BR'>Ações corporais, verbais e mentais
que são parcialmente prejudiciais e parcialmente benéficas.<o:p></o:p></span></p>


<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. Kamma
nem escuro nem claro com um resultado nem sombrio nem luminoso, que conduz à
cessação de kamma: </b></span></b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Essa
é a intenção de transcender os três tipos de kamma mencionados acima, ou em
especial, desenvolver os Sete Fatores da Iluminação ou o Nobre Caminho Óctuplo.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Dos três
canais de kamma – corpo, linguagem e mente – o kamma mental é considerado o
mais importante e de mais longo alcance nos seus efeitos, como demonstrado no
Cânone em Pali:<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>“Desses três tipos de ação,
Tapassi, assim analisadas e diferenciadas, eu descrevo a ação mental como sendo
a mais repreensível para a realização de uma ação prejudicial, para a
perpetração de uma ação prejudicial, e não tanto a ação corporal e a ação
verbal.” [MN.I.373] (<a href="../sutta/MN56.php">MN56</a>)</span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O kamma
mental é considerado o mais importante porque é a origem de todos os demais
kamma. O pensamento precede a ação por meio do corpo e linguagem. Ações
corporais e verbais são derivadas do kamma mental.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Uma dos mais
importantes fatores de influência no kamma mental é <i>ditthi</i> – idéias,
entendimentos, crenças e preferências pessoais. As idéias têm uma influência
importante no comportamento de um indivíduo, nas experiências de vida e ideais
sociais. As ações, linguagem e a manipulação de situações são baseadas em
idéias e preferências. Se existe um entendimento ou idéia incorreta, como
conseqüência qualquer pensamento, linguagem e ações subseqüentes tenderão a
fluir na direção errada. Se existe entendimento correto, então os pensamentos,
linguagem e ações resultantes tenderão a fluir na direção adequada e benéfica.
Isto se aplica não apenas ao nível pessoal mas ao nível social também. Por
exemplo, uma sociedade que mantenha a crença de que a riqueza material é o
objetivo mais precioso e desejável na vida irá se empenhar em obter posses
materiais, medindo o progresso, prestígio e honra por meio da abundância dessas
coisas. O estilo de vida dessas pessoas e o desenvolvimento dessa sociedade
assumirá uma certa forma. Em contraste, uma sociedade que valoriza a paz mental
e o contentamento como objetivo terá um estilo de vida e desenvolvimento
marcadamente distinto.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Existem
muitas ocasiões em que o Buda descreve o entendimento correto, o entendimento
incorreto e a sua importância, como por exemplo:<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'>“E o que, bhikkhus, é entendimento correto? Entendimento correto, eu
digo, tem duas partes: existe o entendimento correto que está sujeito às
impurezas, que participa dos méritos, amadurece onde existe apego; e existe o
entendimento correto que é nobre, imaculado, supramundano, um elemento do
caminho.<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'>“E o que, bhikkhus, é o entendimento correto que está sujeito às
impurezas, que participa dos méritos, amadurece onde existe apego? ‘Existe aquilo
que é dado e o que é oferecido e o que é sacrificado; existe fruto e resultado
de boas e más ações; existe este mundo e o outro mundo; existe a mãe e o pai;
existem seres que renascem espontaneamente; existem no mundo brâmanes e
contemplativos bons e virtuosos que, após terem conhecido e compreendido
diretamente por eles mesmos, proclamam este mundo e o próximo.’ Esse é o
entendimento correto que está sujeito às impurezas, que participa dos méritos,
amadurece onde existe apego.” [MN.III.72] (<a href="../sutta/MN117.php">MN 117</a>)<o:p></o:p></span></p>


<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-language:PT-BR'>* * *<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'>"Bhikkhus, eu não vejo nenhuma outra única coisa, que faz com que qualidades prejudiciais que ainda não surgiram surjam, e qualidades benéficas que já surgiram declinem, como o entendimento incorreto. Para quem tem o entendimento incorreto, as qualidades prejudiciais que ainda não surgiram surgem, e as qualidades benéficas que já surgiram declinam." [<a href="../sutta/ANI.306.php">AN I.306</a>]<o:p></o:p></span></p>


<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-language:PT-BR'>* * *<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>"Bhikkhus, eu não vejo nenhuma outra única coisa, que faz com que qualidades benéficas que ainda não surgiram surjam e qualidades prejudiciais que já surgiram diminuam, como o entendimento correto. Para quem tem o entendimento correto as qualidades benéficas que ainda não surgiram surgem, e qualidades prejudiciais que já surgiram declinam." [<a href="../sutta/ANI.306.php">AN I.307</a>]<o:p></o:p></span></p>


<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-language:PT-BR'>* * *<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>"Bhikkhus, uma pessoa com o entendimento incorreto, qualquer kamma corporal, kamma verbal ou kamma mental que ela incite e realize, de acordo com esse entendimento, e qualquer que seja a sua intenção, anseio, inclinação, e atividades volitivas, tudo irá conduzir ao que não é desejado, não é querido, ao que é desagradável, ao dano e ao sofrimento. Por qual razão? Porque o entendimento é ruim. </p>


<p class=Normal style='margin-left:.75in'>"Suponham, bhikkhus, que uma semente de pepino amargo ou de abóbora amarga fosse plantada num solo fértil. Quaisquer nutrientes que a semente tome do solo e da água irão resultar no seu sabor amargo, pungente, e desagradável. Por qual razão? Porque a semente é ruim. Assim também, uma pessoa com o entendimento incorreto ... tudo irá conduzir ao que não é desejado, não é querido, ao que é desagradável, ao dano e ao sofrimento. Por qual razão? Porque o entendimento é ruim."</p>

<p class=Normal style='margin-left:.75in'>"Bhikkhus, uma pessoa com o entendimento correto qualquer kamma corporal, kamma verbal ou kamma mental que ela incite e realize, de acordo com esse entendimento, e qualquer que seja a sua intenção, anseio, inclinação, e atividades volitivas, tudo irá conduzir ao que é desejado, é querido, ao que é agradável, ao bem-estar e à felicidade. Por qual razão? Porque o entendimento é bom. </p>

<p class=Normal style='margin-left:.75in'>"Suponham, bhikkhus, que uma semente de cana-de-açúcar, de arroz, ou uva,  fosse plantada num solo fértil. Quaisquer nutrientes que a semente tome do solo e da água irão resultar no seu sabor doce, agradável, e delicioso. Por qual razão? Porque a semente é boa. Assim também, uma pessoa com o entendimento correto ... tudo irá conduzir ao que é desejado, é querido, ao que é agradável, ao bem-estar e à felicidade. Por qual razão? Porque o entendimento é bom." [<a href="../sutta/ANI.306.php">AN I.314-315</a>]<o:p></o:p></span></p>


<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-language:PT-BR'>* * *<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'>“Bhikkhus! Há aquele cujo nascimento neste mundo não é para o benefício
de muitos, não é para a felicidade de muitos, mas para a ruína, para o dano de
muitos, para o sofrimento de ambos devas e humanos. Quem é essa pessoa? É a
pessoa com o entendimento incorreto, com idéias distorcidas. Alguém com o
entendimento incorreto conduz muitos para longe da verdade e para a falsidade…<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'>“Bhikkhus! Há aquele cujo nascimento neste mundo é para o benefício de
muitos, é para a felicidade de muitos, para o crescimento, para o benefício,
para a felicidade de devas e humanos. Quem é essa pessoa? É a pessoa com o
entendimento correto, que não tem idéias distorcidas. Alguém com o entendimento
correto conduz muitos para longe da falsidade e para a verdade...”<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'>“Bhikkhus! Eu não vejo outra condição que seja tão danosa como o
entendimento incorreto. Das coisas que causam o dano, bhikkhus, o entendimento
incorreto é a mais importante.” [AN I.33]<o:p></o:p></span></p>


<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-language:PT-BR'>* * *<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'>“Todas as ações são comandadas pela mente:<br>
a mente é o senhor delas, a mente é quem as fabrica.</p>
<p class=Normal style='margin-left:.75in'>Aja ou fale com um estado mental impuro<br>
que o sofrimento virá em seguida da mesma forma<br>
como a roda da carroça segue as pegadas do boi.</p>
