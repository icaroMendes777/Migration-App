<p class=Normal><a name=Introducao></a><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>Introdução</b><o:p></o:p></span></h3>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O trabalho
aqui apresentado está baseado num único capítulo do livro Buddhadhamma, escrito
pelo Venerável P. A. Payutto. Buddhadhamma é talvez a obra mais formal e ambiciosa
do autor até hoje, um volume contendo mais de mil páginas tratando de todo o
conjunto dos ensinamentos do Buda. Embora a abordagem do livro seja erudita,
ele apresenta os temas Budistas, que com freqüência são entendidos de maneira
errada ou são considerados acima do escopo do discípulo leigo comum, de forma
mais acessível em termos práticos.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O venerável
autor é um dos principais sábios Budistas da Tailândia na atualidade. A sua
grande produção literária vai desde simples explanações de temas básicos
Budistas até ensinamentos mais substanciais do Dhamma sob a forma de
comentários (tal como o Buddhadhamma) e análises sociais sob uma perspectiva
Budista<span style="mso-spacerun: yes">  </span>(tal como Buddhist Economics).O
Venerável Payutto possui um talento incomum nesse aspecto, tendo adquirido
experiência em ambas as culturas, Oriental e Ocidental, durante a sua carreira
escolástica e de ensino. Isto combinado com a sua mente inquisitiva, pesquisas
exaustivas e uma compreensão intuitiva rara, proporcionam ao venerável autor
uma posição privilegiada para apresentar os ensinamentos do Buda.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Para o
Ocidental moderno, o ensinamento de kamma oferece um caminho de prática baseado
não no temor de uma autoridade superior ou dogma, mas fundamentado na clara
compreensão da lei natural de causa e efeito no que diz respeito ao
comportamento humano. É um ensinamento não tanto para ser acreditado mas
compreendido e visto em operação.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O Budismo é
uma religião que coloca a sabedoria, ao invés da fé, em primeiro lugar. A
investigação inteligente e honesta não só é bem recebida como também
encorajada. Parte dessa investigação requer uma boa compreensão daquilo que
conduz à maneira como causa e efeito funcionam no nível pessoal. Esse é o
domínio da ética ou moral e é o domínio específico de kamma. Quais os critérios
que existem para o comportamento correto e incorreto? Como conceitos, essas
palavras dão margem a uma extensa série de interpretações, mas no estudo de
kamma estamos preocupados em encontrar definições que sejam práticas e sólidas.
Essas definições precisam não só apontar uma direção clara para a conduta
moral, mas também oferecer as razões e incentivos para mantê-la. O ensinamento
de kamma satisfaz essas exigências.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Falta à
sociedade Ocidental no mundo de hoje clareza ou uma direção coerente nas
questões morais. Com o declínio da fé em um Ser Supremo, seguindo os avanços da
ciência, tudo que parece restar como prescrição para a vida são sistemas
políticos e ideais sociais. Quando o domínio autoritário é rejeitado, com
frequência isso significa uma rejeição de todo o padrão de comportamento
coerente. Parece não haver espaço no pensamento moderno para a virtude, exceto
talvez<span style="mso-spacerun: yes">  </span>no aspecto de ideais, tal como
os direitos humanos.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Na era da
liberdade pessoal e do direito à auto-expressão, a ética parece ter sido
reduzida a uma questão de opinião pessoal, lei social ou preferência cultural.
Conceitos como “certo” e “errado,” “bom” e “mau,” não estão mais assentados
sobre terreno firme e nos encontramos com cada vez mais dificuldades em
definí-los. Essas qualidades são apenas uma questão de opinião, ou elas possuem
algum tipo de realidade baseada na lei da natureza? Como isso se relaciona<span
style="mso-spacerun: yes">  </span>com o mundo científico de relações
impessoais de causa e efeito? Aos olhos de muitos, os conceitos de bom e mau
foram reduzidos a ferramentas de intolerância moral ou oportunismo político. É
por isso que para tantas pessoas é entediante ver ou ouvir a palavra “virtude”;
o tema é definitivamente maçante para a maioria. Numa era em que a vida parece
oferecer uma interminável sucessão de “emoções baratas,” quem está interessado
no autocontrole?<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Apesar
disso, sem uma direção clara na vida nos deparamos com problemas em muitos
níveis. Sem uma direção clara, sem diretrizes nas quais basear a vida, esta se
transforma numa pobre coleção de asneiras, à medida que vamos tateando de forma
desajeitada desta experiência para a seguinte – ainda mais quando o significado
da vida é reduzido a nada mais que uma corrida compulsiva para colecionar
“experiências.”<span style="mso-spacerun: yes">  </span>O resultado é uma sociedade
guiada pelo hedonismo, alimentada pelo desejo e atormentada por problemas: no
nível pessoal, depressão, solidão e desequilíbrio nervoso; no nível social,
comportamento irracional, crime e desordem social. E no nível mais sutil, o
legado da era atual é uma vida em des-sintonia com a natureza, produzindo a
angústia que leva à<span style="mso-spacerun: yes">  </span>busca moderna pela
iluminação baseada em filosofias orientais.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Pois é
justo à luz<span style="mso-spacerun: yes">  </span>dessa situação que a lei de
kamma é tão relevante. Embora as palavras “kamma” ou “karma” [<a href="#N1">1</a>]
<a name=R1></a>sejam algumas vezes ouvidas nos dias de hoje, o conceito
raramente emerge da nuvem de mistério na qual está envolvido desde a sua
introdução no Ocidente. O que é peculiar, porque na verdade a lei de kamma é um
ensinamento em particular dinâmico e lúcido, com especial pertinência para a
era moderna. Na lei de kamma podemos encontrar definições significativas e
relevantes de “bem” e “mal,” um entendimento que não apenas esclarece o caminho
da prática ética, mas também facilita o bem-estar e a realização pessoal. Não
só as necessidades pessoais , mas problemas e direcionamentos no plano social
podem ser compreendidos com mais facilidade com o auxílio deste ensinamento.
Não é portanto surpresa que a Lei de Kamma seja uma das pedras fundamentais do
Budismo.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>É minha
convicção que este livro servirá como referência inestimável para ambos, o
estudante fortuito e o praticante mais comprometido com o Budismo. A lei de
kamma, como um dos temas centrais do Budismo, não requer apenas uma certa dose
de estudo, mas também muita reflexão interna. Este livro portanto não deveria
ser lido somente como uma coleção de idéias para acumular na memória, mas como
idéias sobre as quais refletir, ponderar, avaliar e aplicar na realidade
prática. Alguns dos conceitos apresentados podem à primeira vista parecer
estranhos, mas o tempo gasto em contemplá-los irá revelar que esses conceitos,
longe de serem estranhos, são na verdade bem comuns. Eles são, de fato, tão
comuns que de alguma forma conseguem iludir as nossas mentes complicadas.<o:p></o:p></span></p>


<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-language:PT-BR'>* * *<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>No início
havia decidido fazer uma tradução quase literal deste livro, mas tendo
completado o primeiro rascunho me deparei com um grande número de problemas.
Primeiro, alguns dos pontos abordados no livro se aplicam de modo específico à
cultura Tailandesa e somente têm significado nesse contexto. Uma seção, por
exemplo, abrangia a diferença entre <i>kusala</i> (hábil) e <i>akusala</i>
(inábil) por um lado, e,<span style="mso-spacerun: yes">  </span><i>puñña</i>
(mérito) e <i>papa</i> (demérito) por outro, sendo que essas palavras são usadas
com freqüência na Tailândia. Mas a sua tradução tem limitações e são termos não
muito relevantes em culturas não Budistas. Por essa razão pedi permissão ao
venerável autor para cancelar essa seção. Algumas seções, como a que trata da
intenção, foram movidas de um capítulo para outro. Em todos esses casos busquei
a permissão e o conselho do venerável autor.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Uma das
principais modificações no livro é a adição do Capítulo 4, que lida com kamma
no plano social, que foi composto a partir de uma gravação com o autor de uma
série de perguntas e respostas sobre esse assunto. O tema é na verdade bastante
amplo, digno de<span style="mso-spacerun: yes">  </span>um livro todo dedicado
