</span></span><![endif]><span lang=PT-BR style='mso-ansi-language:PT-BR'>A
crença num eu, <i>(attavada),</i> que irá alcançar ou será impedida de alcançar
os seus desejos<b><o:p></o:p></b></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>9 =&gt;
10. Apego como fator determinante para o ser/existir</span></b><span
lang=PT-BR style='mso-ansi-language:PT-BR'>: O apego condiciona <i>bhava</i>,
estados de vida, tanto no nível de comportamento, <i>(kammabhava),</i> como em
relação ao caráter e propriedades físicas e mentais, <i>(upapattibhava).</i>
Estes poderiam por exemplo ser o padrão de comportamento, <i>(kammabhava),</i>
e traços de caráter, <i>(upapattibhava),</i> de alguém que aspira ficar rico ou
que deseja o poder, fama, beleza, ou que odeia a sociedade e assim por diante.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>10 =&gt;
11. Ser/existir como fator determinante para o nascimento</span></b><span
lang=PT-BR style='mso-ansi-language:PT-BR'>: Dada uma situação de vida para ser
ocupada e possuída, um ser surge para preenchê-la como desfrutador ou
experimentador. Essa é a sensação distinta de ocupar ou possuir aquela situação
de vida. Existe a percepção daquele que age e do que colhe os frutos das ações,
daquele que tem êxito e do que fracassa, daquele que ganha e daquele que perde.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>11 =&gt;
12. Nascimento como fator determinante para o envelhecimento e morte:</span></b><span
lang=PT-BR style='mso-ansi-language:PT-BR'> O nascimento num certo estado traz
consigo de modo inevitável as experiências de prosperidade e declínio que são
parte dele. Estas incluem a iminente degeneração desse estado, as experiências
de adversidade e ruína dentro dele, a separação e a destruição dele. Existe a
constante ameaça do perigo e a necessidade constante de proteger e manter o eu.
A inevitabilidade do declínio e da dissolução, junto com a constante ansiedade
e esforço para proteger esse estado desses fatores, se combinam para causar
tristeza, lamentação, dor, angústia e desespero, ou, o sofrimento.<o:p></o:p></span></p>

<p class=Normal><b><a name=T53></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>Exemplos<o:p></o:p></span></b></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>1 =&gt;
2. Ignorância...formações</span></b><span lang=PT-BR style='mso-ansi-language:
PT-BR'>: Sem conhecer a verdade, conseqüentemente a mente prolifera e imagina,
como um homem que, acreditando em fantasmas, (ignorância), é amedrontrado,
(impulso volitivo), pela luz refletida dos olhos de um animal à noite; ou como
uma pessoa que especula acerca do que outra pessoa tem no seu punho cerrado; ou
como uma pessoa que acredita que os seres celestiais são capazes de criar
qualquer coisa que queiram e inventa cerimonias ou frases místicas como
súplicas; ou como alguém que, desconhecendo a verdadeira natureza das coisas
condicionadas, que são instáveis e sujeitas a fatores determinantes, as vê como
atraentes e desejáveis e deseja obtê-las e controlá-las. Enquanto algum traço
de ignorância ainda estiver presente, impulsos volitivos ou proliferação serão
produzidos.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>2 =&gt;
3. Formações ...consciência</span></b><span lang=PT-BR style='mso-ansi-language:
PT-BR'>: Com <i>cetana,</i> (intenção), e com a coloração mental, a consciência
como visão, audição e assim por diante, é condicionada em conformidade. Sem a
intenção ou interesse, a consciência poderá não surgir, mesmo numa situação em
que isso seja possível. Por exemplo, quando estamos lendo um livro envolvente,
a nossa atenção não se distrai, mas apenas admite para a consciência o assunto
que está sendo objeto de leitura. Até mesmo um ruído forte ou mordidas de
mosquitos podem passar desapercebidos. Quando estamos atentados em buscar um
objeto em particular, poderemos não notar outros objetos.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O mesmo
objeto visto em circunstâncias distintas, com intenções distintas, poderá ser
encarado de forma distinta, dependendo do contexto da intenção. Por exemplo, um
terreno vazio para uma criança pode parecer um ótimo lugar para brincar; para
um homem que tenha a intenção de construir uma casa pode parecer um bom local
para aposentadoria; para um agricultor, outras características serão
importantes, enquanto que para um industrial, ainda outras características serão
proeminentes.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Se olharmos
para o mesmo objeto em momentos distintos, no contexto de pensamentos
distintos, diferentes características irão se destacar mais. Ao pensar
pensamentos benéficos, a mente é influenciada por esses pensamentos, e
interpreta o objeto da atenção nesse contexto. Ao pensar de forma grosseira e
prejudicial, a mente toma nota disso e se dirige e interpreta o significado dos
objetos associados à atenção à luz desses pensamentos destrutivos. Por exemplo,
dentre uma coleção de objetos colocados juntos podem estar uma faca e algumas
flores. Um amante das flores poderá apenas notar as flores e nenhum dos demais
objetos que estejam colocados junto. Quanto mais intenso for o interesse e a
atração por aquelas flores, mais intensa será a atenção dirigida a elas
excluindo todo o restante. Uma outra pessoa que esteja buscando uma arma poderá
apenas notar a faca. No caso de várias pessoas vendo a mesma faca, uma poderá
ter a percepção de uma arma, enquanto que outra poderá ter a percepção de um utensílio
de cozinha, enquanto que uma outra poderá vê-la como uma peça de ferro velho,
tudo dependendo do referencial e da intenção do observador.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>3 =&gt;
4. Consciência...Mentalidade-materialidade (nome e forma)</span></b><span lang=PT-BR
style='mso-ansi-language:PT-BR'>: A consciência e a mentalidade-materialidade (nome e forma)
são interdependentes, tal como dito pelo Venerável Sariputta:<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“É como se dois feixes de junco estivessem em pé se apoiando um no
outro. Da mesma forma, da mentalidade-materialidade (nome e forma) como condição, a
consciência [surge], da consciência como condição, a mentalidade-materialidade (nome e forma).
Se alguém tirasse um dos feixes de junco, o outro cairia. Se alguém tirasse o
outro, o primeiro cairia. Da mesma forma, da cessação da
mentalidade-materialidade (nome e forma), cessa a consciência. Da cessação da consciência,
cessa a mentalidade-materialidade (nome e forma).” [SN.II.114] (<a href="../sutta/SNXII.67.php">SN.XII.67</a>)<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Nesse
contexto, com o surgimento da consciência, a mentalidade-materialidade (nome e forma) surge e
tem que surgir. Como os impulsos volitivos condicionam a consciência, estes
também condicionam a mentalidade-materialidade (nome e forma), mas como a
mentalidade-materialidade (nome e forma) depende da consciência para sua existência, pois são
propriedades da consciência,<span style="mso-spacerun: yes">  </span>diz-se
que: “os impulsos volitivos condicionam a consciência e a consciência
condiciona a mentalidade-materialidade (nome e forma).” Assim podemos analisar a forma como a
consciência condiciona a mentalidade-materialidade (nome e forma) da seguinte forma:<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Quando
se diz que a mente percebe alguma sensação em particular, tal como ao ver ou
ouvir, na verdade é apenas a cognição da mentalidade-materialidade (nome e forma)
(especificamente os <i>khandhas </i>da forma, sensação, percepção e impulsos
volitivos). Tudo que existe no nível experiencial é aquilo que é percebido pela
consciência momento a momento, as propriedades físicas e mentais aparentes para
os sentidos. Quando há cognição, as propriedades mentais e físicas relativas
que são experimentadas existem. A existência de uma rosa, por exemplo, é a
cognição pelo sentido visual naquele momento. Exceto por isso, não há uma
‘rosa’ como tal, a não ser como um conceito na mente. A ‘rosa’ não é
independente das sensações, percepções e conceitos que ocorrem naquele momento.
Assim, quando há consciência, a mentalidade-materialidade (nome e forma) também estará ali de
forma simultânea e independente.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. A
mentalidade-materialidade (nome e forma), especialmente as qualidades mentais que dependem de
um certo momento de consciência irão assumir qualidades em harmonia com aquela
consciência. Sempre que as atividades mentais, ou impulsos volitivos forem
positivos (benéficos ou hábeis), a consciência resultante deles será por
conseqüência clara e alegre e a expressão corporal será espirituosa. Quando os
impulsos volitivos são negativos (prejudiciais ou inábeis) eles levam à
percepção de sensações sob uma perspectiva dura e prejudicial. O estado mental
será negativo e a expressão corporal e o comportamento serão influenciados de
forma correspondente. Nesse estado, os fatores constituintes, tanto mentais
como físicos, estão num estado de alerta para agir em conformidade com os
impulsos volitivos que condicionam a consciência. Quando há um sentimento de
amor e afeição (impulso volitivo) surge a cognição de sensações prazerosas
(consciência), a mente <i>(nama) </i>estará brilhante e alegre, como a
expressão facial <i>(rupa). </i>Com a raiva existe a cognição de sensações desprazerosas,
a mente ficará deprimida e a expressão facial será mal humorada e agressiva.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>No campo
esportivo, o jogador de futebol foca sua atenção e interesse no jogo em
disputa. A sua atenção surge e cessa com intensidade proporcional à força do
interesse dele no jogo. Todos os componentes necessários do corpo e mente estão
preparados para funcionar e realizar as suas tarefas tal como direcionado. A
inter-relação neste caso se refere e inclui o contínuo surgimento e cessação de
mentalidade-materialidade (nome e forma) (ou propriedades físicas e mentais). As propriedades
ativas da mentalidade-materialidade (nome e forma) convergem para formar o estado geral do ser
conforme direcionado pela consciência e pelos impulsos volitivos (observe a
similaridade com <i>bhava</i>).<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Todos os
eventos que ocorrem neste estágio são passos importantes na geração de kamma e
dos seus resultados. O ciclo, ou <i>valla, </i>completou uma pequena rotação
(ignorância é<span style="mso-spacerun: yes">  </span>contaminação ou <i>kilesa;
</i>impulsos volitivos são kamma; a consciência e a mentalidade-materialidade (nome e forma)
são resultados de kamma ou <i>vipaka </i>) e está se preparando para iniciar um
novo ciclo. Este é um estágio importante na formação de hábitos e
características.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>4 =&gt;
5. Mentalidade-materialidade (nome e forma)...seis bases dos sentidos</span></b><span
lang=PT-BR style='mso-ansi-language:PT-BR'>: A mentalidade-materialidade (nome e forma) precisa
funcionar através da consciência do mundo exterior, e junto com a experiência
adquirida previamente será por sua vez usada a serviço da intenção ou impulsos
volitivos. Assim, os componentes da mentalidade-materialidade (nome e forma) que servem como
transmissores e receptores das sensações, (os meios dos sentidos), estão num
estado de alerta para funcionar de acordo com os seus fatores determinantes.
Por exemplo, no caso do jogador de futebol no campo, os órgãos dos sentidos
responsáveis por receber as sensações diretamente concernentes ao esporte que
está sendo praticado, tais como o olho e o ouvido, serão preparados para
receber essas sensações. Ao mesmo tempo, aqueles sentidos não concernentes de
imediato, como o paladar ou o olfato, estarão dormentes ou num estado de
atividade suspensa.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>5 =&gt;
6. As seis bases dos sentidos...contato</span></b><span lang=PT-BR
style='mso-ansi-language:PT-BR'>: A atenção surge através dos meios dos
sentidos, baseada na coordenação de três fatores: os meios dos sentidos
internos, (olho, ouvido, nariz, língua, corpo e mente); objetos sensuais
externos, (visões, sons, aromas, sabores, tangíveis e impressões mentais); e
consciência (através do olho, ouvido, nariz, língua, corpo e mente). A atenção
surge em conformidade com cada meio do sentido em particular.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>6 =&gt;
7. Contato...sensação</span></b><span lang=PT-BR style='mso-ansi-language:PT-BR'>:
Sempre que há o contato tem que haver a experiência de um entre três tipos de
sensações: conforto ou prazer, <i>(sukhavedana);</i> desconforto ou dor <i>(dukkhavedana);</i>
ou indiferença, nem prazer nem dor, (<i>upekkha</i> ou <i>adukkhamasukhavedana</i>).
Do terceiro ao sétimo elo, isto é, de consciência até sensação, é conhecido
como o segmento<span style="mso-spacerun: yes">  </span><i>vipaka, </i>ou
resultado de kamma, do ciclo da Origem Dependente. Os elos 5, 6 e 7, em
particular, não são nem benéficos nem prejudiciais em si mesmos, mas podem ser
os catalisadores para o surgimento de pensamentos e ações benéficas ou
prejudiciais.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>7 =&gt;
8. Sensação...desejo</span></b><span lang=PT-BR style='mso-ansi-language:PT-BR'>:
Quando uma sensação prazerosa é experimentada, o desejo em geral vem em
seguida. Com a sensação desprazerosa, a reação é de estresse, o desejo de fazer
com que o objeto desagradável seja removido ou aniquilado. Também há o desejo
de buscar a distração nas sensações prazerosas. As sensações neutras, ou
indiferentes, induzem a uma condição de embotamento ou complacência. Ambos são
formas sutis e deludidas de sensações prazerosas às quais a mente tende a se
apegar. Elas também podem agir como catalisadoras para a geração de desejo por
mais sensações prazerosas. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O desejo
pode ser dividido em três tipos distintos, assim:<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>1. <i>Kamatanha</i> – Desejo por objetos sensuais.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2. <i>Bhavatanha</i> – ‘Desejo de ser,’ desejo por situações de vida em
particular; num nível mais profundo, isto inclui o instinto pela vida e o
desejo em manter uma certa condição ou identidade.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>3. <i>Vibhavatanha</i> – ‘Desejo de não ser,’ o desejo de escapar ou se
ver livre de objetos ou situações desagradáveis; este tipo de desejo em geral
se expressa por meio de sentimentos como o desespero, depressão, ódio de si
mesmo e auto-piedade.[<a href="#N15">15</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R15></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>O desejo, portanto, aparece sob três formas principais: como desejo por
objetos sensuais, desejo por situações de vida e desejo de se livrar de
situações desagradáveis. Esta última forma de desejo é particularmente evidente
quando os desejos são frustrados ou contrapostos e se expressa por meio do
ressentimento, raiva e agressão.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>8 =&gt;
9. Desejo...apego:</span></b><span lang=PT-BR style='mso-ansi-language:PT-BR'>
Os objetos do desejo se tornam objetos de apego, quanto mais intenso for o
desejo, tanto mais intenso será o apego. O desejo evolve para atitudes e
valores específicos. Com a sensação desagradável, o apego se manifesta como uma
aversão obsessiva em relação ao objeto daquela sensação e um desejo obsessivo
de encontrar uma escapatória dele. Dessa forma, existe apego a objetos dos
sentidos, a situações de vida que podem proporcioná-los, a identidades,
opiniões, teorias e métodos de como obtê-los e ao conceito ou imagem de um eu
que desfruta ou sofre com essas situações.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>9 =&gt;
10. Apego...ser/existir</span></b><span lang=PT-BR style='mso-ansi-language:
PT-BR'>: O apego naturalmente afeta a vida de uma forma ou de outra e os seus
efeitos ocorrem em dois níveis. Primeiro, o apego amarra o eu ou faz com que
ele se identifique com situações particulares na vida, que se acredita poderem
ou satisfazer os desejos, ou proporcionar os meios para escapar das coisas que
não são desejadas. Se existem situações desejadas, naturalmente existirão
situações não desejadas. Esse tipo de situações de vida que são agarradas são
chamadas <i>upapattibhava</i>. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O apego a
qualquer situação de vida irá produzir pensamentos ou intenções ou para que ela
se torne realidade, ou para evitá-la. Esses pensamentos incluirão as
maquinações para inventar meios e maneiras de realizar aqueles desejos. Todos
esses pensamentos e atividades são moldados pela direção e forma de apego. Isto
é, eles operam sob a influência de atitudes, crenças, entendimentos, valores e
gostos ou desgostos acumulados. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Alguns
exemplos simples:<o:p></o:p></span></p>

<p class=Normal style='margin-left:1.0in;text-indent:-.25in;mso-list:l1 level1 lfo4;
tab-stops:list 1.0in'><![if !supportLists]><span lang=PT-BR style='font-family:
Symbol;mso-ansi-language:PT-BR'>•<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><![endif]><span lang=PT-BR style='mso-ansi-language:PT-BR'>Desejo
pelo renascimento num plano celestial irá causar o apego a ensinamentos,
crenças ou práticas que se acredita serem capazes de efetuar tal renascimento,
e o comportamento será condicionado de modo correspondente.<o:p></o:p></span></p>

<p class=Normal style='margin-left:1.0in;text-indent:-.25in;mso-list:l1 level1 lfo4;
tab-stops:list 1.0in'><![if !supportLists]><span lang=PT-BR style='font-family:
Symbol;mso-ansi-language:PT-BR'>•<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><![endif]><span lang=PT-BR style='mso-ansi-language:PT-BR'>O
desejo pela fama irá produzir o apego aos valores e ao comportamento relevante
que se pressupõe necessários para alcançar a fama, e ao eu que irá alcançá-la.
O comportamento resultante será condicionado por aquele apego.<o:p></o:p></span></p>

<p class=Normal style='margin-left:1.0in;text-indent:-.25in;mso-list:l1 level1 lfo4;
tab-stops:list 1.0in'><![if !supportLists]><span lang=PT-BR style='font-family:
Symbol;mso-ansi-language:PT-BR'>•<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><![endif]><span lang=PT-BR style='mso-ansi-language:PT-BR'>O desejo
de adquirir posses que pertencem a outrem irá condicionar os pensamentos de
forma correspondente. O apego habitua o padrão de pensamento que poderá em
algum momento, para aquele que lhe falte circunspecção ou consciência moral,
levar ao roubo. O objetivo original de se tornar um proprietário se transforma
na realidade na existência de um ladrão. Dessa forma, através da busca para
obter objetos dos desejos, as pessoas irão ou criar ações inábeis e desenvolver
hábitos ruins, ou criar ações hábeis e desenvolver a virtude, dependendo da
natureza das suas crenças e entendimento.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O <i>padrão
de comportamento</i> específico resultante da influência do apego, incluindo a
natureza dos eventos também condicionados, é chamado <i>kammabhava</i> (ações
que condicionam o renascimento). As <i>situações de vida</i> que resultam
desses modos de comportamento, quer sejam desejadas ou não, são chamadas <i>upapattibhava</i>
(estados de renascimento).<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Este
estágio do ciclo da Origem Dependente é essencial na criação de kamma e dos
seus resultados, e a longo prazo desempenha um papel crucial no desenvolvimento
de hábitos e traços de caráter.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>10 =&gt;
11. Ser/existir...nascimento</span></b><span lang=PT-BR style='mso-ansi-language:
PT-BR'>: Neste ponto surge o distinto sentimento de um eu, uma identificação
com uma certa situação ou condição, desejada ou não desejada. Na linguagem do
Dhamma poderíamos dizer que um ser surgiu dentro daquele estado <i
style='mso-bidi-font-style:normal'>(bhava)</i>, resultando no sentimento de
alguém que é um ladrão, um proprietário, um sucesso, um fracasso, um joão
ninguém e assim por diante. No caso da pessoa comum, o nascimento, ou o
surgimento da noção do eu, pode ser observado com mais facilidade em tempos de
discórdia, quando o apego tende a surgir de maneiras extremadas. Nas
discussões, mesmo em debates intelectuais, se as contaminações forem usadas no
lugar da sabedoria, uma distinta noção de um eu surgirá na forma de pensamentos
tais como, ‘eu sou superior,’ ‘eu sou o chefe,’ ‘ele é o meu subordinado,’ ‘ele
é inferior,’ ‘essa é a minha opinião,’ ‘minha opinião está sendo refutada,’
‘minha autoridade está sendo questionada’ e assim por diante. Todas essas são
situações em que a identidade está sendo desacreditada ou ameaçada. O
nascimento é portanto mais evidente nos momentos de <i style='mso-bidi-font-style:
normal'>jaramarana</i>, decadência e morte.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>11=&gt;
12. Nascimento…envelhecimento e morte</span></b><span lang=PT-BR
style='mso-ansi-language:PT-BR'>: Dado um eu que ocupa ou assume uma certa
posição, segue que esse eu será cedo ou tarde privado ou separado daquela
posição. O eu é ameaçado pela alienação, frustração, desgraça, conflito e
fracasso. Embora ele busque manter sua posição de modo indefinido, tudo que surge
tem que experimentar a decadência e a dissolução de forma inevitável. Mesmo
antes da dissolução ter início, o eu estará cercado pela ameaça da perdição
iminente. Isso intensifica o apego a situações de vida. O temor da morte surge
da consciência do perigo. O temor da morte e da dissolução estão incrustados
profundamente dentro da mente e estão sempre influenciando o comportamento
humano, causando neuroses, insegurança, a luta desesperada e intensa por
situações de vida desejadas e o desespero face ao sofrimento e a perda. Assim,
para a pessoa comum, o medo da morte assombra toda a felicidade. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Nesse
contexto, quando o eu emerge em qualquer situação de vida não desejada, está
privado de uma situação desejada ou ameaçado por essa possibilidade, ele fica
com o desapontamento e a frustração ou, no idioma Pali, <i style='mso-bidi-font-style:
normal'>soka</i> (tristeza), <i style='mso-bidi-font-style:normal'>parideva</i>
(lamentação), <i style='mso-bidi-font-style:normal'>dukkha</i> (dor), <i
style='mso-bidi-font-style:normal'>domanassa</i> (angústia) e <i
style='mso-bidi-font-style:normal'>upayasa</i> (desespero). Cercado por todo
esse sofrimento, o resultado é a distração e a confusão que são funções da
ignorância. A maioria dos esforços para aliviar o sofrimento são portanto
dirigidos pela ignorância e assim o ciclo continua.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Um exemplo
simples: Para uma pessoa comum que vive num mundo competitivo, o sucesso não se
esgota no mero fenômeno social do sucesso, com todos os seus adornos, mas
inclui o apego à identidade de ser uma pessoa bem sucedida, que é um
‘ser/existir,’ ou estado de vida <i style='mso-bidi-font-style:normal'>(bhava)</i>.
Às vezes o sentimento do eu irá se manifestar através de pensamentos como “eu
sou um sucesso,” que na verdade significa “eu nasci <i style='mso-bidi-font-style:
normal'>(jati) </i>como uma pessoa bem sucedida.” No entanto, esse sucesso, no
seu sentido mais completo, depende de condições externas, tais como a fama,
elogio, alcançar privilégios especiais, admiração e reconhecimento. Nascimento
como um “sucesso,” ou “ser bem sucedido,” depende não somente do reconhecimento
e admiração dos outros, mas da presença de um derrotado, alguém à custa da qual
o sucesso será obtido. Assim que um ser bem sucedido nasce, ele ou ela está
ameaçado com o desaparecimento, anonimidade e perda. Nessa situação, todos os
sentimentos de depressão, preocupação e decepção que não foram tratados de
forma adequada por meio da atenção plena e da plena consciência irão se
acumular no subconsciente e irão exercer influência no comportamento subseqüente
de acordo com o ciclo da Origem Dependente.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Sempre que
ocorre o surgimento do conceito de um eu, existe a ocupação de espaço; onde
existe ocupação de espaço, tem que haver uma fronteira ou limite; quando há
limite, tem que haver separação; quando há separação tem que haver a dualidade
de ‘eu’ e ‘outro.’ O eu irá crescer e se estender para fora através do desejo
para realizar, para agir e impressionar os demais. No entanto, não é possível
que o eu cresça indefinidamente de acordo com os seus desejos. O eu em expansão
irá de modo inevitável encontrar obstáculos de uma forma ou de outra e os
desejos serão frustrados, se não por parte do exterior, então por parte do
interior. Se a pessoa tiver alguma sensibilidade para com a auto-estima dos
outros, o obstáculo irá surgir sob a forma dos próprios escrúpulos. Se não
houver a supressão desses desejos e se for permitido que eles se expressem
totalmente, a oposição irá surgir de fontes externas. Mesmo se fosse possível
se entregar a cada desejo na sua totalidade, tal atividade causaria o
enfraquecimento. Ela apenas serve para aumentar o poder do próprio desejo,
junto com o seu assistente, o sentimento de vazio. O desejo não somente cresce
na dependência de fatores externos, mas ele também faz crescer o conflito
interno. Quando os desejos não são satisfeitos, a tensão, conflito e desespero
são o resultado natural.<o:p></o:p></span></p>

<p class=Normal><b><a name=T54></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>Um exemplo
da Origem Dependente na vida cotidiana<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Tomemos um
exemplo simples de como o princípio da Origem Dependente opera na vida cotidiana.
Suponha que existem dois colegas de escola chamados ‘João’ e ‘José.’ Sempre que
eles se encontram na escola eles sorriem e dizem “Olá” um ao outro. Um dia João
vê José e se aproxima dele com uma saudação amigável preparada, e só obtém o
silêncio e uma expressão azeda como resposta. João se aborrece com isso e deixa
de conversar com José. Neste caso, a cadeia de reações poderia evoluir da
seguinte forma:<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>1.
Ignorância (<i>avijja</i>)</span></b><span lang=PT-BR style='mso-ansi-language:
PT-BR'>: João desconhece a verdadeira razão da careta e do silêncio de José.
Ele falha na atenção com sabedoria sobre o assunto e na averiguação das
verdadeiras razões para o comportamento de José, que poderia não ter nenhuma
relação com os sentimentos dele por João.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>2</span></b><span
lang=PT-BR style='mso-ansi-language:PT-BR'>. <b>Formações (<i>sankhara</i>):</b>
Como resultado, João prossegue pensando e formulando teorias na sua mente,
condicionadas pelo seu temperamento e essas dão origem à dúvida, raiva,
ressentimento, mais uma vez na dependência do seu temperamento.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>3.</span></b><span
lang=PT-BR style='mso-ansi-language:PT-BR'> <b>Consciência (<i>viññana</i>):</b>
Sob a influência dessas contaminações, João segue pensando. Ele presta muita
atenção e interpreta o comportamento e as ações de José<span
style="mso-spacerun: yes">  </span>de acordo com essas impressões anteriores;
quanto mais ele pensa a respeito, mais certeza ele tem; cada gesto de José
parece ofensivo.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>4.
Mentalidade-materialidade (nome e forma) (<i>namarupa</i>):</span></b><span lang=PT-BR
style='mso-ansi-language:PT-BR'> Os sentimentos, pensamentos, estados de
espírito, expressões faciais e gestos de João, isto é, o corpo e mente
combinados, começam a assumir as características gerais de uma pessoa
enraivecida e ofendida, preparada para agir de acordo com essa consciência.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. Bases
dos sentidos (<i>salayatana</i>):</span></b><span lang=PT-BR style='mso-ansi-language:
PT-BR'> Os órgãos dos sentidos de João estão preparados para receber uma
informação que está relacionada e condicionada pela mentalidade-materialidade (nome e forma)
no seu estado de raiva e mágoa.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>6.
Contato (<i>phassa</i>):</span></b><span lang=PT-BR style='mso-ansi-language:
PT-BR'> O choque nos órgãos dos sentidos será das atividades ou atributos de
José que parecem ser em particular relevantes ao caso, tais como olhares
carrancudos, gestos hostis e assim por diante.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>7.
Sensação (<i>vedana</i>):</span></b><span lang=PT-BR style='mso-ansi-language:
PT-BR'> Sensações, condicionadas pelo contato sensual, são do tipo
desagradável.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>8.
Desejo (<i>tanha</i>):</span></b><span lang=PT-BR style='mso-ansi-language:
PT-BR'> <i style='mso-bidi-font-style:normal'>Vibhavatanha</i>, desejo de não
ser, surge o desgosto ou aversão em relação àquela imagem ofensiva, o desejo
que ela desapareça ou seja destruída.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. Apego
(<i>upadana</i>):</span></b><span lang=PT-BR style='mso-ansi-language:PT-BR'>
Segue o apego e o pensamento obsessivo em relação ao comportamento de José. O
comportamento de José é interpretado como um desafio direto; ele é visto como
um disputador e aquela situação exige algum tipo de ação corretiva.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>10.
Ser/existir (<i>bhava</i>):</span></b><span lang=PT-BR style='mso-ansi-language:
PT-BR'> O comportamento subseqüente de João está sob a influência do apego e as
suas ações se tornam antagônicas.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>11.
Nascimento (<i>jati</i>):</span></b><span lang=PT-BR style='mso-ansi-language:
PT-BR'> À medida que o sentimento de inimizade se torna mais distinto, ele é
assumido como uma identidade. A distinção entre ‘eu’ e ‘ele’ se torna mais
evidente e existe um eu que é obrigado a responder de alguma forma à situação.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>12.
Envelhecimento e morte (<i>jaramarana</i>):</span></b><span lang=PT-BR
style='mso-ansi-language:PT-BR'> Esse ‘eu,’ ou condição de inimizade, existe e
prospera dependendo de certas condições, tais como o desejo de aparentar ser
durão, preservar a honra e o orgulho e de ser o vitorioso, sendo que todos
possuem o seu respectivo oposto, tais como sentimentos de inutilidade,
inferioridade e fracasso. Assim que aquele eu surge, ele é confrontado com a
falta de qualquer garantia de vitória. Mesmo se ele obtiver a vitória que
deseja, não há garantia por quanto tempo João será capaz de preservar a sua
supremacia.<span style="mso-spacerun: yes">  </span>Ele pode, na verdade, não
ser o ‘vencedor durão’ que deseja ser, mas ao invés disso o derrotado, o fracote,
aquele que se ridiculariza. Essas possibilidades de sofrimento brincam com o
estado de espírito de João e produzem estresse, insegurança e preocupação. E
estas por seu turno alimentam a ignorância, começando assim uma nova rotação do
ciclo. Esses estados negativos são como feridas supuradas que não foram
tratadas e assim continuam a desprender o seu efeito ‘venenoso’ na consciência
de João, influenciando todo o seu comportamento e causando problemas tanto para
ele mesmo como para os outros. No caso de João, ele poderá se sentir infeliz
por todo aquele dia, falando de forma grosseira com quem quer que tenha contato
com ele e assim aumentando a possibilidade de mais incidentes desagradáveis.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Neste caso,
se fosse para o João praticar da forma correta ele seria aconselhado a começar
com o pé direito. Vendo o mal humor do amigo, ele poderia usar sua<span
style="mso-spacerun: yes">  </span>inteligência (<i>yoniso-manasikara</i>:
aplicando a atenção com sabedoria de acordo com causas e condições) e refletir
que José deveria ter algum problema em mente – ele poderia ter sido censurado
pela mãe, ele poderia estar precisando de dinheiro ou ele poderia simplesmente
estar deprimido. Se João refletisse dessa maneira nenhum incidente teria
ocorrido, a sua mente estaria despreocupada e ele poderia até mesmo ser levado
a agir com compaixão e entendimento.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Uma vez que
a cadeia de eventos tenha sido colocada em movimento, ela pode, no entanto, ser
interrompida através da atenção plena em qualquer ponto. Por exemplo, se a
cadeia de eventos prosseguisse<span style="mso-spacerun: yes">  </span>até o
contato sensual, onde as ações de José foram percebidas de forma negativa, João
ainda poderia ter aplicado a atenção plena exatamente naquele ponto: ao invés
de cair sob o poder do desejo de não ser, ele poderia, ao invés disso,
considerar os fatos da situação e dessa forma obter um outro entendimento do
comportamento de José. Ele poderia então refletir de forma sábia em relação a
ambas as atitudes, as suas e aquelas do seu amigo, de modo que a sua mente não
ficasse oprimida por reações emocionais negativas, mas ao invés disso,
respondesse de uma forma mais clara e positiva. Esse tipo de reflexão, além de
não criar problemas para ele mesmo também poderia servir para encorajar o
surgimento da compaixão.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Antes de
deixarmos de lado este exemplo, poderia ser de ajuda reiterar alguns pontos
importantes: <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>Na vida real, o ciclo completo
     ou cadeia de eventos, tais como aqueles mencionados neste exemplo, ocorrem
     com muita rapidez. Um estudante ao descobrir que não passou num teste,
     alguém que recebe uma má notícia, como a morte de um ente querido, ou um
     homem que vê a sua mulher com um amante, por exemplo, todos podem sentir
     uma enorme tristeza ou choque, até mesmo sentindo os joelhos fraquejarem,
     gritando ou desmaiando. Quanto mais intenso for o apego e a ligação
     emocional, mais intensa será a reação.<o:p></o:p></span></p>
<p class=Normal style='margin-left:.75in'>Deve ser enfatizado uma vez
     mais que a inter-relação dentro dessa cadeia de eventos não tem que ser
     obrigatoriamente em ordem seqüencial, da mesma forma como o giz, quadro
     negro e a escrita são todos fatores determinantes para as letras brancas
     na superfície do quadro negro, mas não têm que aparecer em ordem
     seqüencial.<o:p></o:p></span></p>
<p class=Normal style='margin-left:.75in'>O ensinamento da Origem
     Dependente objetiva esclarecer a mecânica da natureza, analisar o
     desdobramento dos eventos à medida que estes ocorrem, para que as causas possam
     ser identificadas com mais facilidade e corrigidas. Quanto aos detalhes de
     como essa correção pode ser efetuada, isso não diz respeito aos
     ensinamentos da Origem Dependente, mas, ao invés disso, pertencem ao
     domínio de <i>magga,</i> (o Caminho), ou o Caminho do Meio.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em todo
caso, os exemplos dados aqui são bastante simplificados e podem parecer um
tanto superficiais. Eles não estão detalhados o suficiente para transmitir a
completa sutileza do princípio da Origem Dependente, em especial naquelas
seções da ignorância como fator determinante para os impulsos volitivos, e
tristeza, lamentação e desespero condicionando mais uma rotação do ciclo.
Olhando para o nosso exemplo, pode parecer que o ciclo apenas surge às vezes,
que a ignorância é um fenômeno esporádico e que a pessoa comum pode passar
longos períodos da sua vida sem o surgimento da ignorância. Na verdade, para o
ser não iluminado, a ignorância em graus variados se encontra por trás de cada
pensamento, ação e palavra. O nível mais básico dessa ignorância é simplesmente
a percepção de que existe um eu que está pensando, falando e agindo. Se esse
aspecto não estiver presente na mente, a verdadeira relevância dos ensinamentos
para a vida diária terá sido negligenciada. Por essa razão alguns dos aspectos mais
profundos dessa cadeia de eventos serão examinados a seguir em mais detalhe.<o:p></o:p></span></p>
