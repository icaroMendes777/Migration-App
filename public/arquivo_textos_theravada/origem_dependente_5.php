<p class=Normal style='margin-left:.5in'><span lang=PT-BR
style='mso-ansi-language:PT-BR'><a href="#T54">Um exemplo da Origem Dependente na vida cotidiana</a><o:p></o:p></span></p>

<p class=Normal>&nbsp;</p>

<hr size=2 width="100%" align=center>

<p class=Normal>&nbsp;</p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A descrição
da Origem Dependente dada no capítulo anterior é aquela encontrada com mais
freqüência nas escrituras e comentários. Ela busca explicar a Origem Dependente
como <i>samsaravatta</i>, o ciclo de renascimentos, mostrando as conexões entre
três vidas – o passado, o presente e o futuro.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Aqueles que
não concordarem com essa interpretação, ou que preferirem algo mais imediato,
poderão encontrar outras alternativas não só no Abhidhamma Pitaka, onde o
princípio da Origem Dependente é mostrado na sua totalidade em um momento
mental, como também poderão interpretar as mesmas palavras do Buda empregadas
para sustentar o modelo padrão sob um ângulo distinto, proporcionando um quadro
bastante diferente do princípio da Origem Dependente, algo que é defendido
pelos ensinamentos e referências textuais de outras fontes.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os
argumentos empregados para defender tais interpretações são muitos. Por
exemplo, o conhecimento direto do fim do sofrimento e a vida sem aflições do
Arahant são estados que podem surgir nesta presente vida. Não é necessário
morrer antes de realizar a cessação do nascimento, envelhecimento e morte e do
mesmo modo com a tristeza, lamentação, dor, angústia e desespero. Essas coisas
podem ser superadas nesta mesma vida. O ciclo todo da Origem Dependente, tanto
na origem do sofrimento como na sua cessação, diz respeito à vida presente. Se
o ciclo puder ser claramente compreendido da forma como opera no presente,
conseqüentemente o passado e o futuro também serão compreendidos de forma
clara, porque eles são todos parte do mesmo ciclo.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Como
referência, considere as palavras do Buda:<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Udayin, se alguém fosse recordar as suas muitas vidas passadas, isto é,
um nascimento, dois nascimentos... assim, nos seus modos e detalhes, se ele se
recordasse das suas muitas vidas passadas, então um dos dois, ou ele me
perguntaria uma questão sobre o passado, ou eu poderia perguntar-lhe uma
questão sobre o passado, e ele poderia satisfazer a minha mente com a resposta
à minha questão ou eu poderia satisfazer a mente dele com a minha resposta à
questão dele. Se alguém por meio do olho divino, que é purificado e sobrepuja o
humano, vê seres falecendo e renascendo, inferiores e superiores, bonitos e
feios, afortunados e desafortunados... e compreende como os seres continuam de
acordo com as suas ações, então um dos dois, ou ele me perguntaria uma questão
sobre o futuro, ou eu poderia perguntar a ele uma questão sobre o futuro, e ele
poderia satisfazer a minha mente com a resposta à minha questão ou eu poderia
satisfazer a mente dele com a minha resposta à questão dele. Mas deixemos de
lado o passado, Udayin, deixemos de lado o futuro. Eu lhe ensinarei o Dhamma:
Quando existe isso, aquilo existe; Com o surgimento disso, aquilo surge. Quando
não existe isso, aquilo também não existe; Com a cessação disto, aquilo cessa.”
[M.II.31](<a href="../sutta/MN79.php">MN 79</a>)<o:p></o:p></span></p>

<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-language:PT-BR'>* * *<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Então, o chefe tribal Bhadraka foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse: "Seria bom, venerável senhor, se o Abençoado me ensinasse sobre a origem e a cessação do sofrimento." </p><o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>O Abençoado respondeu, “Chefe tribal, se eu lhe ensinasse a origem e a cessação do sofrimento referindo-me ao passado assim, ‘assim era no passado,’ a dúvida e a perplexidade surgiriam em você por conta disso. Se eu lhe ensinasse a origem e a cessação do sofrimento referindo-me ao futuro assim, ‘assim será no futuro,’ a dúvida e a perplexidade surgiriam em você por conta disso. Ao invés disso, chefe tribal, enquanto estou sentado exatamente aqui, e você está sentado exatamente aí, eu lhe ensinarei a origem e a cessação do sofrimento." [SN.IV.327] (<a href="../sutta/SNXLII.11.php">SN XLII.11</a>)<o:p></o:p></span></p>


<p class=Normal align=center style='margin-left:.75in;text-align:center'><span
lang=PT-BR style='mso-ansi-language:PT-BR'>* * *<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Produzidas
por distúrbios da bílis, Sivaka, surgem certos tipos de sensações. Que isso
acontece, pode ser compreendido pela própria pessoa e também no mundo é aceito
como verdadeiro. Produzidas por distúrbios da fleuma ... dos ventos ... de um desequilíbrio [dos três] ... 
pela mudança de clima .. pelo comportamento descuidado ... pela violência ... 
pelos resultados de Kamma – por meio de tudo isso, Sivaka, surgem
certos tipos de sensações. Que isso acontece, pode ser compreendido pela
própria pessoa e também no mundo é aceito como verdadeiro. <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Agora quando esses contemplativos e brâmanes possuem esta doutrina e
entendimento: ‘Tudo aquilo que uma pessoa experimentar, quer seja prazer, dor
ou nem prazer, nem dor, tudo é causado pelas ações passadas,’ então eles vão
além daquilo que conhecem por si mesmos e daquilo que é aceito no mundo como
verdadeiro. Portanto, eu digo que esses contemplativos e brâmanes estão
equivocados.” [SN.IV.230](<a href="../sutta/SNXXXVI.21.php">SN.XXXVI.21</a>)<o:p></o:p></span></p>

<p class=Normal align=center style='margin-left:.75in;text-align:center'><span
lang=PT-BR style='mso-ansi-language:PT-BR'>* * *<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Bhikkhus, aquilo que alguém intenciona, aquilo que alguém planeja, e
qualquer coisa pela qual alguém tenha preferência: isto se torna uma base para a
manutenção da consciência. Quando há uma base, haverá um suporte para o
estabelecimento da consciência. Quando a consciência se estabelece e cresce, há
a produção de uma renovada existência futura. Quando há a produção de uma
renovada existência futura, o futuro nascimento, e morte, lamentação, dor,
angústia e desespero surgem. Essa é a origem de toda essa massa de sofrimento.”
[SN.II.65] (<a href="../sutta/SNXII.38.php">SN XII.38</a>)<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Embora esta
interpretação do princípio da Origem Dependente deva ser entendida pelo que ela
é, apesar disso não descartamos a forma estabelecida pelo modelo padrão. Então,
antes de explorar o seu significado deveríamos primeiro reiterar o modelo
padrão, adaptando as definições de acordo com esta interpretação.<o:p></o:p></span></p>

<p class=Normal><b><a name=T51></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>Definição
preliminar<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. <b>Ignorância</b>
– ignorância da verdade, ou das coisas como elas são; estar deludido pelas
realidades nominais; a ignorância que está por detrás das crenças; falta de
sabedoria; inabilidade em compreender o ciclo de causa e efeito.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. <b>Formações</b>
– atividades mentais, intenção deliberada, intenção e decisão e a sua geração
de ações; a organização da maneira de pensar de acordo com hábitos acumulados, habilidades,
preferências e crenças; o condicionamento da mente e da maneira de pensar.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. <b>Consciência</b>
– a consciência das sensações, isto é: visão, audição, olfato, paladar, toque e
cognição, o estado básico da mente de momento a momento.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. <b>Mentalidade-materialidade (nome e forma)</b>
(o organismo animado) – a presença da corporalidade e mentalidade como parte da
consciência; o estado de coordenação entre o corpo e a mente para que funcionem
de acordo com o fluxo da consciência; as mudanças corporais e mentais como resultado
dos estados mentais.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>5. <b>As
seis bases do sentidos</b> – o funcionamento dos meios dos sentidos.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>6. <b>Contato</b>
– o ponto de contato entre a consciência e o mundo externo.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>7. <b>Sensação
</b>– de prazer, de dor ou indiferença.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>8. <b>Desejo
– </b>o desejo de buscar objetos sensuais prazerosos e de escapar dos
desprazerosos. O desejo pode ser de três tipos: desejo de ter e desfrutar,
desejo de ser e desejo de destruir ou livrar-se.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>9. <b>Apego
– </b>apego e agarramento a sensações prazerosas ou desprazerosas, às condições
de vida que arrojam essas sensações e a avaliação e a atitude em relação a
essas coisas com base no seu potencial para satisfazer desejos.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>10. <b>Ser/existir
</b>– todo o processo de comportamento gerado para satisfazer o desejo e o
apego (<i>kammabhava </i>- o processo ativo); e também as condições da vida que
resultam dessas forças (<i>upapattibhava</i> – o processo passivo).[<a
href="#N14">14</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R14></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>11. <b>Nascimento </b>– claro reconhecimento da emergência em um estado de
existência; identificação com estados de vida ou modos de conduta e a noção
resultante de quem os desfruta, ocupa ou experimenta.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>12. <b>Envelhecimento
e morte</b> – a consciência da separação ou privação do eu de um estado de
existência ou identidade; a sensação de ameaça de aniquilação ou separação
desses estados de ser; e daí, a experiência resultante de tristeza, lamentação,
dor, angústia e desespero (mesmo nas suas formas mais sutis).<o:p></o:p></span></p>

<p class=Normal><b><a name=T52></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>Como os
elos se conectam<o:p></o:p></span></b></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>1 =&gt;
2.</span></b><span lang=PT-BR style='mso-ansi-language:PT-BR'> <b>Ignorância
como fator determinante para as formações</b>: Sem conhecimento ou consciência
da verdade, sem a compreensão clara ou reflexão sábia sobre as experiências, o
resultado é o raciocínio confuso baseado na conjectura e na imaginação e
condicionado pelas crenças, temores e traços de caráter acumulados. Como
conseqüência, isso condiciona todas as decisões ao agir, falar ou pensar.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>2 =&gt;
3. Formações como fatores determinantes para a consciência</span></b><span
lang=PT-BR style='mso-ansi-language:PT-BR'>: Com a intenção, a consciência é
condicionada como conseqüência. Temos a tendência, (ou estamos condicionados),
a ver, ouvir e cogitar de acordo com<span style="mso-spacerun: yes">  </span>as
influências das nossas intenções arraigadas. Além disso, o contexto dentro do
qual vemos, ouvimos ou cogitamos também estará condicionado por essas
intenções. A intenção irá conduzir a consciência a repetidamente se recordar e
proliferar acerca de certos eventos. A intenção também irá condicionar o estado
de espirito básico, ou consciência, a assumir ou qualidades finas e boas, ou
grosseiras e más; a consciência é condicionada de acordo com as intenções boas
ou más.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>3 =&gt;
4. Consciência como fator determinante para a mentalidade-materialidade (nome e forma)</span></b><span
lang=PT-BR style='mso-ansi-language:PT-BR'>: A cognição, visão, audição e assim
por diante, implicam as propriedades físicas, (<i>rupadhamma</i>), e as
propriedades mentais, (<i>namadhamma</i>), que conhecemos e vemos. Além disso,
quando a consciência opera, as propriedades físicas e mentais relevantes,
(sendo elas a ‘corte’ da consciência – os <i>khandhas </i>da forma, sensação,
percepção e impulsos volitivos), também precisam funcionar de acordo e de modo
coordenado com a natureza daquela consciência. Por exemplo, quando a
consciência está moldada pela raiva, as percepções que surgem como resultado
serão igualmente negativas. O corpo irá assumir características em conformidade
com a intenção hostil, tais como expressões faciais agressivas, tensão nos
músculos e pressão sangüínea alta. As sensações serão desagradáveis. Quando a
consciência assume qualquer característica em particular de forma repetida e
habitual, as propriedades mentais e físicas subseqüentes se tornarão os traços
corporais e mentais de comportamento e caráter correspondentes.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>4 =&gt;
5. Mentalidade-materialidade (nome e forma) como fatores determinantes para as seis bases dos
sentidos</span></b><span lang=PT-BR style='mso-ansi-language:PT-BR'>: Quando a
mentalidade-materialidade (nome e forma) funciona, os meios dos sentidos relevantes são
ativados para satisfazer a sua demanda, (buscando informação relevante ou
desfrutando de sensações). Essas portas dos sentidos irão funcionar
de acordo com os estados corporais e mentais que as condicionam.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>5 =&gt;
6. As seis bases dos sentidos como fatores determinantes para o contato</span></b><span
lang=PT-BR style='mso-ansi-language:PT-BR'>: Com o funcionamento das várias
portas dos sentidos, o contato, <i>(phassa), </i>o choque com elas ou
a plena consciência das sensações, surge na dependência da porta do sentido em
questão estar funcionando naquele momento.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>6 =&gt;
7. Contato como fator determinante para a sensação</span></b><span lang=PT-BR
style='mso-ansi-language:PT-BR'>: Juntamente com a consciência tem que haver
sensações de um tipo ou outro: se não prazerosa ou desprazerosa, então neutra.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>7 =&gt;
8. Sensação como fator determinante para o desejo</span></b><span lang=PT-BR
style='mso-ansi-language:PT-BR'>: Com a experiência de sensações prazerosas
segue o deleite e o apego. Isto é o desejo sensual, <i>(kamatanha)</i>. Algumas
vezes há o desejo por uma posição da qual será possível controlar e desfrutar
dessas sensações prazerosas. Este é o desejo de ser ou por estados de ser, <i>(bhavatanha)</i>.
As experiências que produzem sensações de desconforto ou sofrimento em geral
provocam pensamentos de aversão e o desejo de se livrar da fonte daquelas
sensações. Este é o desejo por não ser, <i>(vibhavatanha).</i> Entre as
sensações neutras, tais como a indiferença ou embotamento, existe um apego
sutil, de tal forma que a indiferença é considerada como uma forma sutil de
sensação agradável, propensa a evoluir para o desejo por formas mais aparentes
de prazer a qualquer instante.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>8 =&gt;
9. Desejo como fator determinante para o apego</span></b><span lang=PT-BR
style='mso-ansi-language:PT-BR'>: À medida que o desejo se torna mais forte,
ele se transforma em apego, um tipo de preocupação mental, criando uma atitude
para com o objeto do desejo e a avaliação desse objeto; (com <i>vibhavatanha</i>,
uma avaliação negativa será formada). Uma posição fixa é adotada em relação às
coisas: se houver atração esta precipitará um efeito aglutinante, uma
identificação com o objeto da atração. Qualquer coisa conectada com aquele
objeto parecerá boa. Quando existe repulsa, o objeto daquela repulsa parece
afrontar o eu. Qualquer posição adotada em relação a essas coisas tende a
reforçar o apego, que será dirigido para, e por outro lado irá reforçar o valor
de: <o:p></o:p></span></p>

