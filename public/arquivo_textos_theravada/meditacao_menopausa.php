<p class=Normal><b>Amherst, Massachusetts</b> - Um novo estudo diz que um método de meditação muito fácil de ser aprendido pode ajudar aliviar as ondas de calor, os suores noturnos e a insônia da menopausa. </p>

<p class=Normal>O estudo feito na Universidade de Massachusetts mostrou que a meditação Budista da atenção plena reduziu o sofrimento associado com as ondas de calor e melhorou a condição física, psicossocial e sexual das participantes. </p>

<p class=Normal>"Os resultados são importantes porque a terapia de reposição hormonal, empregada para tratar os sintomas da menopausa está associada a riscos para a saúde," disse James Carmody, o autor do estudo, que é um professor de medicina na divisão de medicina preventiva e comportamental. </p>

<p class=Normal>Os pesquisadores observaram que cerca de 40% das mulheres na menopausa sofrem com ondas de calor e suor noturno, o que compromete a sua qualidade de vida. Mas visto que a terapia de reposição hormonal foi relacionada a um aumento no risco de doenças coronárias, câncer de mama e avc, Carmody observou que "não somente as  mulheres estão buscando terapias alternativas, mas também é uma prioridade do NIH (National Institutes of Health) encontrar tratamentos comportamentais." </p>

<p class=Normal>De acordo com o estudo, nenhum outro tratamento que substitua a terapia de reposição hormonal foi encontrado, mas o treinamento na meditação da atenção plena parece possibilitar que as mulheres sejam "menos reativas" aos sintomas da menopausa. </p>

<p class=Normal>A meditação da atenção plena ajuda focar no presente. As praticantes evitam fazer julgamentos e simplesmente aceitam o que quer que esteja acontecendo na mente enquanto dirigem a sua atenção para a respiração. Os especialistas observam que não é difícil aprender a técnica, mas alguma disciplina é necessária no princípio. </p>

<p class=Normal>O objetivo dos pesquisadores foi influenciar a reação das mulheres aos sintomas, "incluindo a aflição psicológica, o desconforto social e a ansiedade." </p>

<p class=Normal>"Queríamos ver se poderíamos influenciar a resiliência das mulheres em resposta a esses sintomas," explicou Carmody. "Não estávamos tentando influenciar os sintomas em si, embora estes também tenham tido algum efeito." </p>

<p class=Normal>O estudo dividiu 110 mulheres com idade entre 47 e 69 anos em dois grupos, um que recebeu o treinamento, e o outro na "fila de espera" aguardando o treinamento. </p>

<p class=Normal>As participantes preencheram um questionário para determinar os fatores que se sabe influenciam as ondas de calor como por exemplo o consumo de álcool, a prática de yoga e exercícios. </p>

<p class=Normal>Os pesquisadores também mediram quatro dimensões de qualidade de vida: as funções física, psicológica, vasomotora (ondas de calor) e sexual. As mulheres avaliaram o incômodo causado pelos sintomas numa escala de quatro pontos, variando "de modo nenhum" a "extremamente" incomodada. Elas mantiveram um diário anotando o número e a intensidade das ondas de calor e do suor noturno. Na média as mulheres tinham no início do estudo,. diariamente, cinco ou mais ocorrências de ondas de calor ou suor noturno, moderadas a severas. </p>

<p class=Normal>Depois de participarem no treinamento uma vez por semana ao longo de oito semanas e mais um dia inteiro, as mulheres no grupo do treinamento experimentaram uma redução de 15% na intensidade do incômodo dos sintomas contra 7% no grupo de controle. Embora a intensidade das ondas de calor não fosse muito diferente, o grupo do treinamento relatou melhor sono e menos ansiedade e percepção de estresse. </p>

<p class=Normal>No começo do estudo, que ocorreu de Novembro de 2005 a Setembro de 2007, as participantes tinham problemas de sono com "significância clínica." A melhora  no sono foi um importante resultado do estudo. </p>

<p class=Normal>"O que mais nos surpreendeu foi o efeito no sono," disse Carmody, notando ter sido observado que o treinamento da atenção plena foi tão eficaz quanto a terapia de reposição hormonal na redução da insônia. </p>

<p class=Normal>Outro especialista elogiou o estudo por utilizar a "conexão mente-corpo" para ajudar as mulheres com sérios sintomas de menopausa, sem "efeitos colaterais." </p>

<p class=Normal>"Sabíamos da conexão mente-corpo," disse a Dra. Jill M. Rabin. "Estamos apenas começando a desvendar o poder da mente para impactar a nossa identidade psicológica." </p>

<p class=Normal>O estudo foi publicado na edição de Junho de 2011 da revista Menopause. </p> 

 



