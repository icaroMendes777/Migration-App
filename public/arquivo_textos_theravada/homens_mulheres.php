<p class=Normal>Uma das importantes regras do Vinaya, para os monges bem como as monjas Budistas, é a proibição de um monge estar a sós com mulheres, e uma monja estar a sós com homens. Esta não é apenas uma regra para os monges e monjas mais rigorosos, mas uma regra que o Buda definiu como obrigatória para todos os monges e monjas. Visto que esta é uma regra que muitas vezes vem à tona, vou explicá-la em detalhe neste artigo sobre o Vinaya.</p>

   <p class=Normal> Na introdução de uma das regras encontrada no Vinaya Pitaka (<i>Aniyata 1</i>), é mencionada a seguinte história:</p>

 <p class=Normal style='margin-left:.75in'>       "Certa ocasião o Abençoado estava em Savatthi, no Bosque de Jeta, no Parque de Anathapindika. Então o Venerável Udayin foi até aquela jovem (a filha recém-casada de um dos patrocinadores do Venerável Udayin) e tendo se aproximado dela, sentou-se junto daquela jovem, um homem e uma mulher, em um lugar oculto, em um assento isolado, conveniente, conversando na hora certa, falando sobre o Dhamma na hora certa ... Visakha (a famosa discípula leiga) viu o Venerável Udayin sentado junto daquela jovem, um homem e uma mulher, em um lugar oculto, em um assento isolado, conveniente. Vendo aquilo, ela disse  para o Venerável Udayin: 'Isso não é apropriado, venerável senhor, não é adequado que o mestre sente-se junto com as mulheres, um homem e uma mulher, em um lugar oculto, em um assento isolado, conveniente. Embora o mestre não tenha nenhum desejo por aquilo (relação sexual), as pessoas incrédulas são difíceis de serem convencidas.' O Venerável Udayin não deu atenção a Visakha ... Visakha comentou o assunto com os monges ... Os monges ficaram contrariados, aborrecidos e irritados e relataram o assunto para o Abençoado ... o Abençoado repreendeu o Venerável Udayin: 'Como pode você, homem tolo, sentar-se junto com as mulheres, um homem e uma mulher, em um lugar oculto, em um assento isolado, conveniente?'"

 <p class=Normal>              ... ( O Buda então estabeleceu uma regra explicando ... ) </p>

 <p class=Normal>       'Um lugar oculto' significa oculto do olho, oculto do ouvido. Oculto do olho significa que o espectador será incapaz de ver se o monge está piscando o olho, levantando a sobrancelha, movendo a cabeça. Oculto do ouvido significa que o espectador é incapaz de ouvir o que é dito em um tom de voz normal. 'Um assento isolado' significa que é separado por uma parede, ou por uma porta fechada, ou por uma tela, ou por um arbusto grande, ou por uma coluna, ou por um saco, ou que seja ocultado por qualquer coisa de outro tipo. 'Conveniente' significa ser possível realizar um  ato sexual".</p>


 <p class=Normal>    A regra seguinte, <i>Aniyata 2</i>, é semelhante à primeira, que acabei de descrever, com a única diferença que desta vez o Venerável Udayin sentou-se com a mesma jovem, só os dois, em um lugar oculto (oculto do olho, oculto do ouvido), que não era um assento isolado (por uma parede etc.), nem um lugar conveniente (para o ato sexual). Mais uma vez quando Visakha avistou  o Venerável Udayin e a jovem a sós, ela repreendeu o monge:</p>

<p class=Normal style='margin-left:.75in'>"Isso não é apropriado, venerável senhor, não é adequado que o mestre sente-se junto com as mulheres, um homem e uma mulher, em um lugar oculto. Embora o mestre não tenha nenhum desejo por aquilo (relação sexual), as pessoas incrédulas são difíceis de serem convencidas."</p>

 <p class=Normal>Novamente o Venerável Udayin não deu atenção a Visakha, assim ela comentou o assunto com os monges que relataram o assunto para o Buda. O Buda repreendeu o Venerável Udayin e condenou esse tipo de comportamento através da criação de uma outra regra.</p>

<p class=Normal>As citações acima, diretamente do Vinaya Pitaka, mostram o perigo de um monge estar a sós com mulheres, especialmente dentro de um quarto, em um carro ou edifício. Estas regras foram repetidas no Vinaya das monjas, proibindo uma monja de estar a sós com homens, por razões semelhantes. Na sociedade de hoje, onde as alegações de abuso sexual são abundantes, e com freqüência são a palavra de uma pessoa contra a palavra de outra, manter essas regras importantes é mais do que apenas uma proteção contra a possibilidade de abuso, mas também uma proteção contra as suspeitas espalhadas pelos maliciosos. Suspeitas, mesmo que falsas, são muito difíceis de refutar. Como disse a sábia leiga Visakha: "Embora o mestre não tenha nenhum desejo por aquilo (relação sexual), as pessoas incrédulas são difíceis de serem convencidas."</p>

<p class=Normal>Conhecendo agora esta regra, que todos possamos ajudar os monges e monjas Budistas a manterem os seus preceitos corretamente e, assim, evitar alegações que prejudicam a todos. Por favor, evite ficar a sós com um monge, se você é uma mulher, e se você é um homem, evite ficar a sós com uma monja. Muito obrigado.</p>

