<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O Buda era
como um médico, tratando os males espirituais da raça humana. O caminho da
prática que ele ensinou é como um tratamento
para corações e mentes sofredoras. Essa forma de compreender o Buda e os seus
ensinamentos remonta aos textos mais antigos e no entanto é também bastante
atual. A prática de meditação Budista é freqüentemente anunciada como um meio
de cura e atualmente um número significativo de psicoterapeutas recomendam aos
seus pacientes que tentem a meditação como parte do seu tratamento. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>No entanto, depois de
muitos anos ensinando e praticando a meditação como terapia, muitas pessoas
descobriram que a meditação por si só não é suficiente. Baseado na minha
própria experiência, descobri que os meditadores ocidentais tendem a ser mais
afligidos por um certo amargor e carência de auto-estima que quaisquer
Asiáticos que tenham sido meus alunos. As suas mentes estão tão machucadas pela
civilização moderna que lhes falta a resiliência e a persistência necessárias
para que a prática da concentração e insight possam ser genuinamente
terapêuticas. Outros professores também observaram esse problema e como
resultado, muitos deles decidiram que o caminho Budista não é suficiente para
as nossas necessidades em particular. Para compensar essa insuficiência eles
têm experimentado outros meios para complementar a prática de meditação,
combinando-a com coisas como mitos, poesia, psicoterapia, ativismo social,
saunas, rituais matinais e até mesmo tocar tambor. O problema, no entanto, pode
não ser a falta de algo no caminho Budista, mas sim o fato de nós não termos
seguido o tratamento completo do Buda. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O caminho Budista consiste
não só na prática de atenção plena, concentração e insight, mas também na
virtude, começando pelos cinco preceitos. Na verdade, os preceitos constituem o
primeiro passo no caminho. Existe uma tendência no Ocidente de descartar os
cinco preceitos como regras da escola dominical atadas às normas culturais
antigas que não mais se aplicam à nossa moderna sociedade, mas isso omite o
papel que o Buda intencionava: os preceitos são parte do tratamento para mentes
machucadas. Especialmente, eles têm como objetivo curar dois padecimentos
subjacentes à baixa auto-estima: remorso e negação. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando as nossas ações não
estão à altura de um certo padrão de comportamento, ou nós (1) sentimos
remorso, ou (2) nos ocupamos com um dos dois tipos de negação, (a) negando que
as nossas ações na verdade tenham ocorrido, ou (b) negando que os padrões de
medição sejam realmente válidos. Essas reações são como feridas na mente. O
remorso é uma ferida aberta, sensível ao toque, enquanto que a negação é como a
cicatriz endurecida, retorcida em torno de um ponto sensível. Quando a mente
está ferida dessa forma, ela não é capaz de se estabelecer com conforto no
presente, pois ela está repousando sobre a carne esfolada, exposta, ou sobre
nós calcificados. Mesmo quando ela é forçada a permanecer no presente, ela está
ali apenas de modo tenso, distorcido e parcial, e assim os insights que ela
obtém tendem também a serem distorcidos e parciais. Só se a mente estiver livre
de feridas e cicatrizes poderemos esperar que ela se estabeleça com conforto e
liberdade no presente, e que faça surgir o discernimento sem distorções.<span
style="mso-spacerun: yes">  </span><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>É nisso que entram os cinco
preceitos: eles foram desenhados para curar essas feridas e cicatrizes. A
auto-estima saudável provém do fato de estarmos à altura de um conjunto de
padrões que são práticos, bem definidos, humanos e dignos de respeito; os cinco
preceitos estão formulados de tal forma que proporcionam justamente esse
conjunto de padrões. <o:p></o:p></span></p>

<p class=Normal><i><span lang=PT-BR style='mso-ansi-language:PT-BR'>Práticos:</span></i><span
lang=PT-BR style='mso-ansi-language:PT-BR'> Os padrões definidos pelos
preceitos são simples – não matar de forma intencional, não roubar, não ter
sexo impróprio, não mentir, não ingerir embriagantes. É totalmente possível viver
de acordo com esses padrões. Nem sempre é fácil ou conveniente, mas sempre é
possível. Eu presenciei esforços para traduzir os preceitos em padrões que soem
mais eminentes ou nobres – tomando, por exemplo, o segundo preceito e ampliando
o seu significado para:<span style="mso-spacerun: yes">  </span>não abusar dos
recursos do planeta – mas mesmo as pessoas que reformularam os preceitos dessa
forma admitem ser impossível honrá-los. Qualquer um que tenha lidado com
pessoas que sofreram danos psicológicos sabe que quase sempre o dano provém de
ter-lhes sido conferido padrões impossíveis de serem honrados. Se você puder
dar às pessoas padrões que requerem pouco esforço e atenção, e possíveis de
serem cumpridos, a auto-estima delas aumentará de forma dramática à medida que
elas descobrirem que realmente são capazes de estar à altura desses padrões.
Elas poderão, então, enfrentar tarefas mais exigentes com confiança em si
mesmas. <o:p></o:p></span></p>

<p class=Normal><i><span lang=PT-BR style='mso-ansi-language:PT-BR'>Bem definidos: </span></i><span
lang=PT-BR style='mso-ansi-language:PT-BR;mso-bidi-font-style:italic'>Os
preceitos são formulados sem ‘se’ ou ‘mas.’ Isso significa que eles
proporcionam orientação muito clara, sem espaço para o palavreado vago ou
racionalizações não muito honestas. Uma ação se encaixa nos preceitos ou não.
Mais uma vez, os padrões desse tipo são uma referência de vida muito saudável.
Qualquer um que tenha educado filhos se deu conta que embora eles venham a
reclamar das regras estritas, eles na verdade se sentem mais seguros com elas
do que com regras indefinidas e sempre abertas à negociação. Regras bem
definidas não permitem que agendas ocultas entrem de forma sorrateira pela
porta de trás da mente. Se, por exemplo, o preceito contra o ato de matar
permitisse que você matasse seres vivos quando a presença deles fosse
inconveniente, isso colocaria a sua conveniência num nível mais elevado do que
a sua compaixão pela vida. A conveniência se tornaria o seu padrão oculto – e
como todos nós sabemos, os padrões ocultos proporcionam superfícies enormes de
terreno fértil para que a hipocrisia e a negação floresçam. Se, no entanto,
você se mantiver fiel aos padrões dos preceitos, então tal como dito pelo Buda,
você estará proporcionando segurança ilimitada para as vidas de todos. Não
existe nenhuma circunstância sob a qual você tiraria a vida de qualquer ser
vivo, não importa quão inconveniente eles possam ser. Quanto aos demais
preceitos, você estará proporcionando segurança ilimitada para os bens e a
sexualidade de todos e honestidade e atenção plena ilimitadas na sua
comunicação com eles. Ao descobrir que é possível confiar em si mesmo nesses
casos, você obtém uma sensação inegável de auto-respeito.</span><span
lang=PT-BR style='mso-ansi-language:PT-BR'> <o:p></o:p></span></p>

<p class=Normal><i><span lang=PT-BR style='mso-ansi-language:PT-BR'>Humanos:</span></i><span
lang=PT-BR style='mso-ansi-language:PT-BR'> Os preceitos são humanos tanto para
as pessoas que os observam como para as pessoas afetadas pelas ações daquelas.
Se você os respeitar estará se alinhando com a doutrina de karma que ensina que
as forças mais importantes que moldam a sua experiência do mundo são os
pensamentos, palavras e atos intencionais que você escolhe no presente momento.
Isso significa que você não é insignificante. A cada momento que você fizer uma
escolha – em casa, no trabalho, se entretendo – você estará exercendo o seu
poder na contínua moldagem do mundo. Ao mesmo tempo, este princípio permite que
você meça a si mesmo dentro de padrões totalmente sob o seu controle: as suas
ações intencionais no momento presente. Em outras palavras, eles não o forçam a
medir a si mesmo em relação à aparência, força, inteligência, talento
financeiro, ou qualquer outro critério que dependa menos do seu karma presente
do que do seu karma do passado. Eles também<span style="mso-spacerun: yes"> 
</span>não manipulam os seus sentimentos de culpa ou forçam-no a lamentar os
lapsos do passado. Ao invés disso, eles focam a sua atenção na possibilidade
sempre presente de viver de acordo com os seus padrões no aqui e agora. Se você
estiver vivendo com pessoas que seguem os preceitos, você descobrirá que os
assuntos que você tratar com elas não serão motivo de desconfiança ou temor.
Elas consideram o seu desejo pela felicidade similar ao delas. O valor delas
como indivíduos não depende de situações nas quais tenha que haver ganhadores e
perdedores. Quando elas falam em desenvolver amor bondade e atenção plena na
meditação, você vê isso refletido nas ações delas. Dessa maneira, os preceitos
estimulam não somente indivíduos saudáveis, mas também uma sociedade saudável –
uma sociedade na qual o auto-respeito e o respeito mútuo não estão em conflito.
<o:p></o:p></span></p>

<p class=Normal><i><span lang=PT-BR style='mso-ansi-language:PT-BR'>Digno de respeito: </span></i><span
lang=PT-BR style='mso-ansi-language:PT-BR;mso-bidi-font-style:italic'>Quando
você adota um conjunto de padrões, é importante saber a quem pertencem esses
padrões e ver de onde vieram esses padrões, pois na realidade você estará se
ajuntando ao grupo deles, buscando a aprovação deles e aceitando o critério
deles quanto ao que é certo e errado.</span><span lang=PT-BR style='mso-ansi-language:
PT-BR'> Neste caso, você não poderia ter um grupo melhor ao qual se unir: o
Buda e os seus nobres discípulos. Os cinco preceitos são chamados de “virtudes
apreciadas pelos nobres .” Tomando por base aquilo que os textos nos dizem dos
nobres, eles não são pessoas que aceitam padrões simplesmente com base na
popularidade. Eles colocaram as suas vidas em risco para descobrir o que conduz
à verdadeira felicidade, e viram por si mesmos, por exemplo, que toda mentira é
patológica e que qualquer relação sexual fora de um relacionamento estável,
comprometido, é insegura. Outras pessoas poderão não respeitá-lo por viver de
acordo com os cinco preceitos, mas os nobres irão respeitá-lo, e o respeito
deles vale mais do que o respeito de qualquer outra pessoa no mundo.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Agora, muitas pessoas
consideram pouco objetivo unir-se a um grupo tão abstrato, especialmente quando
ainda não tiveram a oportunidade de conhecer um nobre pessoalmente. É difícil
ter bom coração e ser generoso quando a sociedade à sua volta se ri abertamente
dessas qualidades e ao invés disso valoriza coisas como proezas sexuais ou
práticas de negócios predatórias. É nesse aspecto que as comunidades Budistas
poderiam desempenhar um papel. Seria muito útil se grupos Budistas se
separassem abertamente dos prevalentes métodos amorais da nossa cultura e
deixassem claro, de uma forma gentil, que eles valorizam a bondade e o
autocontrole entre os seus membros. Fazendo isso, eles proporcionariam um
ambiente saudável para a adoção plena do tratamento do Buda: a prática da
concentração e discernimento numa vida com ações virtuosas. Quando encontramos
esse tipo de ambiente, descobrimos que a meditação não necessita de<span
style="mso-spacerun: yes">  </span>mitos ou simulações para suportá-la, porque
ela está baseada na realidade de uma vida bem vivida. Você poderá olhar para os
padrões dentro dos quais está vivendo e depois inspirar e expirar com conforto
– não como uma flor ou uma montanha, mas como um ser humano completo e <a
name=PVW>responsável</a>. Pois é isso que você é. <o:p></o:p></span></p>


