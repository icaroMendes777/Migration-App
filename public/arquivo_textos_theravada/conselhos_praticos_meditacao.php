<p class=Normal><a name=ch7></a><b>A Concentração e o seu Aperfeiçoamento<br>
<i >(Samadhi-parami)<o:p></o:p></b></i> <a
href="#Top"><span style='text-decoration:none;text-underline:none'><img border=0 title="Topo" src="../img/tup.gif"></span></a>
</p>

<p class=Normal>Tomando em conta os significados dessa palavra em conjunto com outros termos
especializados como desenvolvimento da mente <i style='mso-bidi-font-style:
normal'>(bhavana),</i> absorções <i style='mso-bidi-font-style:normal'>(jhana),</i>
insight <i style='mso-bidi-font-style:normal'>(vipassana),</i> unicidade da
mente em um só ponto <i style='mso-bidi-font-style:normal'>(ekaggata)</i> e
exercício de meditação <i style='mso-bidi-font-style:normal'>(kammatthana),</i>
poderemos agora examinar o que constitui a concentração perfeita. O que distingue
particularmente a boa prática Budista, quer seja de um Bodisatva ou não,
daquela de um meditador comum, (de qualquer religião), é que este último irá
muito provavelmente se apegar firmemente aos prazeres que ocorrem nos níveis
superiores do reino da esfera sensual, ou aos puros prazeres do reino da
matéria sutil e, como resultado, irá renascer em um desses estados divinos. Se
uma pessoa se deixar aprisionar em um desses destinos, onde os prazeres e
alegrias são muitos e os sofrimentos mínimos, então é improvável que ela venha
ser capaz de gerar a energia necessária para o aperfeiçoamento da sabedoria.
Por isso, o bom meditador se torna proficiente nas absorções, (de forma que ele
possa penetrá-las quando quiser e emergir quando quiser), sem no entanto
apegar-se a elas. Mas deve-se notar que isso se aplica apenas ao meditador
habilidoso que já conquistou as absorções. Se alguém ainda não alcançou esses
níveis, então a aspiração ardente, não o desapego, será a atitude correta. </p>

<p class=Normal>Depois que essas absorções tenham sido alcançadas, elas podem ser examinadas
como impermanentes, insatisfatórias e desprovidas de um eu ou alma, <i
style='mso-bidi-font-style:normal'>(aniccam,</i> <i style='mso-bidi-font-style:
normal'>dukkham,</i> <i style='mso-bidi-font-style:normal'>anatta),</i> nesse
momento o desapego em relação às absorções irá surgir naturalmente e o insight, <i
style='mso-bidi-font-style:normal'>(vipassana)</i>, será experimentado. As
absorções, (e os poderes que podem surgir em conexão com elas), são portanto, no
método Budista de treinamento, nunca um fim em si mesmas mas são sempre usadas
para promover o insight e a sabedoria que surgem quando a mente concentrada
toma a tarefa de examinar a mente e o corpo para conhecer de forma completa as
suas características.<span style="mso-spacerun: yes">   </span></p>

<p class=Normal>Uma história que exprime o significado desta perfeição é a vida de
Kuddalamuni. O seu nome significa o sábio do Alvião e ele era assim chamado
devido à dificuldade que tinha para livrar-se do seu apego ao seu alvião. Em
muitas ocasiões ao deixar a sua casa com a intenção de meditar na floresta, ele
era arrastado de volta pela memória do seu alvião e da sua antiga ocupação de
agricultor. Um dia, refletindo acerca da inconstância com a qual ele se
dedicava à meditação, ele tomou o seu alvião e girando-o sobre a sua cabeça o
arremessou nas profundezas do Ganges. Tendo feito isso, ele explodiu com um
grande grito de alegria. O rajá local, que estava passando por ali com o seu
exército, enviou um homem para investigar porque aquele agricultor estava tão
feliz, o sábio replicou relatando a sua experiência. O rajá e muitos outros
ficaram muito impressionados com a resposta e alguns o seguiram para se dedicar
à vida meditativa na floresta, depois do que, nos contam, todos faleceram para
experimentar a vida no reino da matéria sutil. O sábio do alvião que não era
outro que Gotama em uma vida passada, exibindo um outro aspecto da perfeição da
meditação: a habilidade para treinar outros na meditação após ele mesmo ter
obtido proficiência nela. </p>

<p class=Normal>Por fim, podemos acrescentar algumas notas resumidas sobre alguns dos
perigos da prática de meditação. </p>


<p class=Normal>&nbsp;</p>

