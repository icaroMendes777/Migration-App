<p class=Normal><a name=ch2></a><b>Duas Correntes de Meditação</b> <a
href="#Top"><span style='text-decoration:none;text-underline:none'><img border=0 title="Topo" src="../img/tup.gif"></span></a>
</p>

<p class=Normal>Duas correntes de meditação Budista, aparentemente distintas, podem ser
distinguidas, embora elas sejam vistas como complementares quando a meditação estiver
estabelecida. </p>

<p class=Normal>Pode ser produtivo para algumas pessoas, cujas mentes são muito ativas e que
sofrem com a distração, acompanhar com atenção plena as acrobacias da mente
traquinas. Como a mente é na verdade uma série de eventos mentais que surgem e
desaparecem com incrível rapidez, sendo que, cada um desses eventos é uma mente
completa com os seus fatores mentais de apoio, no início este tipo de atenção
plena é na verdade uma mente &quot;plenamente atenta&quot; observando outras
&quot;mentes&quot; (que logicamente estão todas dentro do próprio contínuo
mental da pessoa). Através disso a pessoa desenvolve a habilidade de olhar para
a mente e ver para onde ela está indo. Ela foi para o passado, presente ou
futuro ? Foi para a materialidade, ou para as sensações, ou talvez para as
percepções, para as atividades volitivas, ou ela foi para a consciência?
Através deste método, &quot;Para onde ela foi”?, a mente distraída, pouco a
pouco, irá se encontrar sob a vigilância da mente plenamente atenta, até que a
atenção plena crie uma base sólida para desenvolvimentos adicionais.
Importante, embora mundana, a clareza mental é ao mesmo tempo necessária e
desenvolvida através desta prática, que no entanto deve ser equilibrada pela<span
style="mso-spacerun: yes">  </span>tranqüilidade das absorções <i
style='mso-bidi-font-style:normal'>(jhanas). </i>Quando a mente tiver se
acalmado, a pessoa deve começar a praticar a concentração <i style='mso-bidi-font-style:
normal'>(jhanas),</i> que será por seu lado a base para o surgimento do
verdadeiro insight. Este método é denominado &quot;a sabedoria que conduz à
tranqüilidade&quot;. </p>

<p class=Normal>Outro método, adequado para aquelas pessoas cujas mentes no início não são
tão perturbadas, inclui os tradicionais quarenta objetos de meditação (veja o <a
href="#ch9">Apêndice</a>); e esses envolvem o uso de um objeto esepcifico para
a concentração. Esse objeto pode ser o próprio corpo ou uma parte dele, uma cor
ou um desenho, uma palavra ou uma frase, ou a contemplação abstrata e assim por
diante. Todos esses métodos envolvem um tanto de disciplina mental, firme porém
gentil, em que cada vez que a mente começar a vagar, ela precisa ser trazida de
volta gentilmente (com a atenção plena, é lógico), para se concentrar novamente
no objeto escolhido. </p>

<p class=Normal>Algumas pessoas possuem a idéia equivocada de que esse tipo de prática deve
necessariamente conduzir à tranqüilidade, praticamente de imediato. Elas
poderão se surpreender ao iniciar a prática, visto que, de fato experimentarão
mais problemas do que tinham antes. Isso ocorre, primeiro, porque elas na
verdade nunca olharam antes dentro da própria mente para saber o estado em que
ela sempre esteve; e segundo, porque ao disciplinar a mente, é como se a pessoa
agitasse um lago estagnado com uma vara , ou cutucasse com uma vara um fogo latente
sob as cinzas. O elefante selvagem da mente, há muito tempo acostumado a vagar
pela floresta dos desejos, não aceita a domesticação de bom grado, ou ser
amarrado ao poste da prática com as correias da atenção plena. No entanto, a
diligência e o zelo irão finalmente assegurar os frutos da tranqüilidade. </p>

<p class=Normal>Todos os quarenta objetos fazem parte do segundo método em que a calma
obtida com a prática é então usada para despertar a sabedoria. Por isso eles
são chamados de métodos da &quot;tranqüilidade que conduz à sabedoria&quot; e
são muito importantes nos tempos atuais de distrações. A sua explicação
completa pode ser encontrada no <i style='mso-bidi-font-style:normal'>Path of
Purification</i> <i style='mso-bidi-font-style:normal'>(Visuddhi-magga)</i>,
embora nem mesmo o grande ensinamento contido nesse livro possa substituir o
contato pessoal com um mestre.<span style='font-size:10.0pt;mso-bidi-font-size:
12.0pt'> <o:p></o:p></span></p>


<p class=Normal>&nbsp;</p>

