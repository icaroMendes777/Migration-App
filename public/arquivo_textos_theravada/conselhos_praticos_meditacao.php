<p class=Normal><a name=ch3></a><b>Conselhos Práticos para Meditadores</b> <a
href="#Top"><span style='text-decoration:none;text-underline:none'><img border=0 title="Topo" src="../img/tup.gif"></span></a>
</p>

<p class=Normal>Mudando do aspecto psicológico para o prático, a meditação para pessoas
leigas pode ser dividida em duas categorias: aquela que é feita de forma
intensiva e aquela que é praticada em conjunto com os demais afazeres da vida
diária. A meditação intensiva também é de dois tipos: meditação sentada diária
e a prática ocasional em retiros.</p>

<p class=Normal><b>Meditação Sentada Diária</b> <a
href="#Top"><span style='text-decoration:none;text-underline:none'><img border=0 title="Topo" src="../img/tup.gif"></span></a>
</p>

<p class=Normal>Primeiro discutiremos o período diário regular de prática intensiva que
deveria, sempre que possível, ocorrer todos os dias na mesma hora. A pessoa
deve evitar que isto se torne apenas um ritual, encarando a prática com
seriedade e permanecendo intensamente consciente da razão por que medita. As
sugestões a seguir também podem ajudar. </p>

<p class=Normal>Quanto às condições físicas, o lugar para meditar deveria ser bem
silencioso. Se tiver um pequeno quarto que possa ser usado para esse propósito,
tanto melhor, e de todos os modos é melhor meditar só, exceto se outros membros
da casa também meditarem. Quando esse for o caso a pessoa deve garantir a
pureza da sua mente em relação aos outros, pois de outra forma a cobiça, a
raiva e todo o resto da quadrilha de ladrões, com certeza, irão roubar os
frutos da meditação. </p>

<p class=Normal>O silêncio pode ser conseguido despertando cedo, antes que os demais
despertem; e essa também é a ocasião em que a mente está clara e o corpo
descansado. Portanto, o meditador sincero observa os horários, pois ele sabe
como é importante ter horas de sono em quantidade suficiente para despertar
renovado. </p>

<p class=Normal>Após despertar e lavar-se a pessoa deve sentar-se com roupas limpas e
confortáveis no local de meditação. A pessoa pode ter um pequeno altar com
objetos Budistas, mas isso não é essencial. Algumas pessoas consideram benéfico
começar fazendo oferendas de flores, incenso e velas, refletindo com cuidado
enquanto fazem isso. É muito comum nos países Budistas prefaciar a meditação
silenciosa com um cântico suave para si mesmo, &quot;<i>Namo tassa bhagavato
arahato samma-sambuddhassa</i>,&quot; com os <a href="../caminho_liberdade/refugio_triplice.php#formula"> Refúgios</a> e 
<a href="../caminho_liberdade/cinco_preceitos.php">Preceitos</a>. Se a pessoa
conhece as passagens em Pali em homenagem ao Buda, Dhamma e Sangha, elas também
podem ser usadas nesse momento. </p>

<p class=Normal>Uma outra prática preliminar proveitosa é uma reflexão, uma recitação
discursiva, sobre algumas verdades do Dhamma, tal como o trecho sugerido a
seguir: </p>

<p class=Normal style='margin-top:5.0pt;margin-right:.5in;margin-bottom:
5.0pt;margin-left:.75in'>&quot;Obtendo esta preciosa oportunidade de um
nascimento humano, tenho duas responsabilidades no Dhamma: o meu próprio
benefício e o benefício dos outros. Todos os demais seres, quer sejam humanos
ou não humanos, visíveis ou invisíveis, grandes ou pequenos, próximos ou distantes,
todos esses seres eu tratarei com nobreza e desejarei que eles vivam em paz.
Que eles possam ser felizes…Que eles possam ser felizes…Que eles possam ser
felizes…! Eu os ajudarei quando estiverem sofrendo e compartirei das suas
alegrias quando estiverem felizes. Que eu possa também desenvolver a
incomparável equanimidade, a mente em perfeito equilíbrio que nunca está
perturbada!<span style="mso-spacerun: yes">    </span></p>

<p class=Normal style='margin-top:5.0pt;margin-right:.5in;margin-bottom:
5.0pt;margin-left:.75in'>&quot;Ao me preocupar com o bem estar dos outros, eu não
me esquecerei do meu próprio progresso no caminho do Dhamma. Que eu venha a
entender, realmente, como, empurrado aqui e ali pelos ventos do kamma, sofri
uma infinidade de vidas em todos os planos de existência! Eu também devo voltar
a minha mente para o quão curta e passageira é esta vida. Que a mente e o corpo
estão mudando constantemente, surgindo e desaparecendo momento a momento. Que
nem a mente nem o corpo me pertencem, nenhum deles é meu. Eu também devo voltar
a minha mente para tomar em consideração o quanto esta breve vida é importunada
por problemas. Tendo sido gerado pelo desejo e nascido da ignorância, preciso
compreender que não há como escapar da morte, que o envelhecimento e a
enfermidade são acontecimentos naturais da minha condição. Preciso me esforçar
para entender, por meu próprio benefício e benefício de outros, que esta pessoa
chamada 'eu' é um complexo de mente e materialidade em que nenhuma entidade
permanente, tal como uma alma ou eu, pode ser encontrado. </p>

<p class=Normal style='margin-top:5.0pt;margin-right:.5in;margin-bottom:
5.0pt;margin-left:.75in'>&quot;Que eu possa através desta prática experimentar o
insight da impermanência, sofrimento e não-eu! Que eu possa ser um dos que permanece
no Vazio! E tendo entendido essa verdade sublime que eu possa mostrar o caminho
para outros!&quot;<span style="mso-spacerun: yes">   </span></p>

<p class=Normal>Ao sentar para meditar, deve-se tomar cuidado para que o
corpo permaneça ereto, porém relaxado. Não deve haver tensão, mas, tampouco a
cabeça deve baixar e nem a região lombar ceder. O corpo deve ser sentido
aprumado e equilibrado. Embora a posição com as pernas cruzadas (tal como a
postura de lótus) seja a ideal se o meditador estiver sentado sobre um tapete
suficientemente macio, uma cadeira pode ser usada por aqueles desabituados com
a postura de lótus ou então incapazes de treinar para sentar nessa posição.
Sentar na postura em lótus ou meio lótus será muito mais fácil se uma almofada
mais firme for usada para levantar as nádegas. Dessa forma os joelhos irão
tocar o solo e uma firme posição sentada apoiada sobre três pontos (dois
joelhos e as nádegas) será obtida. </p>

<p class=Normal>O tempo de meditação deve ser o mesmo todos os dias até que, tendo mais
habilidade em concentrar a mente, automaticamente a pessoa irá ter vontade de
ampliar a prática. Um método amplamente usado para medir o período de meditação
é meditar durante o tempo que leva para consumir uma vareta de incenso. Tendo
colocado as mãos relaxadas sobre o regaço em posição para a meditação, os olhos
podem ser cerrados ou deixados semi abertos de acordo com o que for mais
confortável. </p>

<p class=Normal>Os métodos usados para auxiliar na concentração da mente são muitos e as
duas principais vertentes na meditação clássica foram brevemente comentadas na
seção acima. Outros métodos que podem ajudar incluem a repetição de uma palavra
ou frase, quiçá com o uso de um rosário. Se a pessoa praticar a atenção plena
focada na respiração pode ser que considere bom o uso de uma palavra como
&quot;Buddho,&quot; ou &quot;Araham&quot; para acalmar a mente. A primeira
sílaba é repetida silenciosamente ao inspirar e a pessoa se concentra na
segunda sílaba ao expirar. A contagem da respiração também (em geral não acima
de dez para evitar que a mente vagueie) é usada como um auxílio para a
concentração. Mas todos esses auxílios devem ser abandonados quando a
concentração se tornar aprimorada. Quando a meditação se utiliza de apenas uma
frase, um rosário pode ser usado em conjunto, cada repetição marcada por uma
conta. </p>

<p class=Normal>A meditação estará se desenvolvendo bem se a pessoa se der conta de que a
mente está cada vez mais absorvida no objeto de meditação escolhido, mas a
pessoa não deve deduzir que a meditação é inútil apenas porque por um período
longo ou curto não forem experimentadas muito mais do que sonolência e
distrações. Esses <a href="../cinco_obstaculos.php"> obstáculos</a> devem ser enfrentados; e se eles surgirem, não
será através da irritação ou desespero, mas através da observação calma, com
atenção plena, que eles poderão ser superados. Para obter-se êxito, é
necessário uma grande persistência e esforço equilibrado.<span
style="mso-spacerun: yes">  </span></p>

<p class=Normal>O período de meditação pode ser encerrado com alguma recitação, cujo objeto
normalmente é o bem-estar e a distribuição dos méritos para outras pessoas.
Uma tradução, ou o original em Pali, do <a href="../sutta/SnpI8.php">Karaniya
Metta Sutta</a> <i style='mso-bidi-font-style:normal'>(O Discurso do Amor
Bondade)</i> pode ser recitado nesse momento e como não é um texto muito longo,
pode ser memorizado facilmente. Como os métodos de recitação podem variar, pode
ser útil obter-se gravações feitas por bhikkhus dos trechos que a pessoa queira
aprender. </p>

<p class=Normal>Aproveitando o tema de recitação, é muito útil conhecer alguns discursos do
Buda em algum dos idiomas clássicos do Budismo e de usá-los para unificar a
mente se houver alguma ocasião em que nenhuma concentração possa ser obtida.
Nessas ocasiões o meditador não deve se sentir deprimido mas deve continuar
sentado, recitando suavemente para si mesmo. Isso é o que os monges Budistas
fazem duas vezes por dia como parte do seu desenvolvimento mental e é também
útil para estimular uma abordagem mais devocional, necessária, como contrapeso
para os indivíduos com inclinações intelectuais. Um outro método proveitoso
para a superação da distração é a meditação andando, que pode ser feita em
qualquer corredor da casa ou em uma área isolada no jardim. Uma distância de
vinte ou trinta passos será suficiente, pois se for mais longa, a mente tenderá
a vagar e se for mais curta, a distração poderá aumentar. A pessoa pode
caminhar no rítmo que for natural, com as mãos juntas e os braços relaxados na
frente do corpo. Ao final da caminhada a pessoa deve fazer a volta na direção
horária.[<a name=BM3></a><a href="#n1">1</a>] </p>

<p class=Normal><a name=r1></a>Talvez seja adequado mencionar algumas palavras sobre
devoção, pois ela é muito importante na prática de meditação. Ninguém que não
seja um Budista devoto adotaria a meditação <i style='mso-bidi-font-style:normal'>Budista,
</i>pela simples razão de que ele não teria os ideais Budistas presentes no seu
coração. Tomar com seriedade o <a href="../caminho_liberdade/refugio_triplice.php">Refúgio Tríplice</a> e a compreensão da Jóia
Tríplice está intimamente ligado com a meditação Budista. Um Budista realmente
devoto, que dedica toda sua vida ao Dhamma, não enfrentará dificuldades
insuperáveis na prática de meditação. Quaisquer obstáculos que ele encontre,
ele irá superá-los, sustentado pela devoção. Ele está preparado para um caminho
longo e difícil, pois ele compreende que ele é quem o fez<span
style="mso-spacerun: yes">  </span>assim. Se ele encontrar o seu caminho
bloqueado, a sua meditação não progredindo e ele próprio sem um mestre, ele não
vacilará ou hesitará no caminho. Ele pensa, &quot;Eu estou agora experimentando
os resultados de ações intencionais <i style='mso-bidi-font-style:normal'>(kamma)
</i>praticadas por mim no passado&quot;. E ele se recorda das <a href="../sutta/DN16.php#T6_7">últimas palavras</a>
do Buda: &quot;Dessa forma, bhikkhus, eu os encorajo: todas as coisas condicionadas estão sujeitas 
à dissolução. Esforçem-se pelo objetivo com diligência&quot;. Todas as dificuldades são coisas
condicionadas e por fim irão mudar; enquanto isso, muito pode ser realizado com
atenção plena, energia e devoção. </p>

<p class=Normal>Se a pessoa não se sentir demasiado cansada após o trabalho e se houver uma
oportunidade à noite, um outro período de meditação poderá ser feito. De
qualquer modo, antes de dormir, praticar meditação é um hábito sábio, mesmo que
sejam apenas alguns minutos, de forma a purificar a mente antes de deitar. A pessoa
pode pensar da seguinte forma: &quot;Não existe certeza, ao me deitar, que
irei despertar&quot;. Uma pessoa pode, portanto, estar se deitando para morrer
e essa é uma boa reflexão para estimular estados mentais hábeis e benéficos e
banir estados inábeis e prejudiciais. Se a pessoa praticar dessa forma,
&quot;deitar-se pronta para morrer&quot;, será uma excelente preparação para o
acontecimento real, que irá ocorrer de qualquer forma em algum momento no
futuro. Pode até mesmo gerar as condições adequadas para o surgimento do
insight que permite à pessoa &quot;morrer&quot;, soltando-se do apego às coisas
que não lhe pertencem, isto é, a mente e o corpo. A repetição de uma frase ou
palavra do Dhamma também pode ser usada até que a pessoa adormeça. Dessa forma
o dia se inicia e se encerra com a prática dos ensinamentos Budistas. E além da
dedicação o dia inteiro a eles, o que poderia ser melhor? <span
style='font-size:10.0pt;mso-bidi-font-size:12.0pt'><o:p></o:p></span></p>

<p class=Normal><b>Retiros</b> <a
href="#Top"><span style='text-decoration:none;text-underline:none'><img border=0 title="Topo" src="../img/tup.gif"></span></a>
</p>

<p class=Normal>Com relação à segunda divisão da prática
intensiva, isto é, quando feita em retiro, muito irá depender das facilidades
disponíveis para o estudante sério. Existem alguns lugares no Ocidente em que
se pode buscar instruções de meditação. A coisa mais importante é ter contato
direto com um mestre de meditação capaz (livros servem no início, pois até
mesmo um mestre pode, mais tarde, se mostrar deficiente de alguma forma). Após
satisfazer essa condição, uma outra se faz necessária: a pessoa tem que se
esforçar com diligência para praticar e realizar os ensinamentos. Se essas duas
condições forem satisfeitas, então essa pessoa será uma das mais afortunadas
entre os seres humanos. <o:p></o:p></span></p>

<p class=Normal>Muitos não terão acesso a um mestre e alguns podem querer tentar um período
de meditação solitária em alguma parte remota do seu país. Isso deveria ser
tentado apenas se a pessoa já desenvolveu um bom poder de atenção plena. De
outra forma, aquilo que se intencionava como estímulo para a meditação pode se
transformar num período inútil e até mesmo acompanhado pela aparente
intensificação das contaminações mentais. </p>

<p class=Normal><b>Vida Diária</b> <a
href="#Top"><span style='text-decoration:none;text-underline:none'><img border=0 title="Topo" src="../img/tup.gif"></span></a>
</p>

<p class=Normal>Quanto ao outro tipo de prática meditativa que é feita na vida diária,
embora muito possa ser escrito a respeito, as seguintes breves palavras podem
servir como um guia. Primeiro, a pessoa não deve enganar a si mesma com relação
à concentração da mente. Não faz nenhum sentido enganar a si mesmo ou aos
outros de que a vida diária <i style='mso-bidi-font-style:normal'>é </i>meditação
- a não ser é claro que alguém já possua grandes poderes dde concentração.
Somente uma pessoa muito experiente, com freqüência alguém que tenha praticado
durante muitos anos observando a disciplina monástica é capaz de perceber a
vida do dia a dia como meditação; e é muito improvável que tal pessoa contasse
para os outros esse fato. Recusando-se a dar ao orgulho uma oportunidade para
distorcer a verdadeira situação do seu estado mental, a pessoa deveria ser leal
para consigo mesma e admitir as suas próprias limitações. Isso já seria um
grande passo adiante. A pessoa leiga que já se considera um <i
style='mso-bidi-font-style:normal'>Arahant</i> criou de maneira muito eficaz um
bloqueio para o verdadeiro progresso; enquanto que a pessoa honesta tem pelo
menos a sabedoria de ser humilde.<span style="mso-spacerun: yes">    </span></p>

<p class=Normal>Muito pode ser alcançado com a atenção plena, enquanto que sem ela não
existe nenhuma esperança de meditação na vida diária. Como eventos comuns podem
ser transformados em meditação? Por meio da atenção plena que, para começar,
pode ser definida como <i style='mso-bidi-font-style:normal'>consciência
daquilo que se está fazendo no presente momento. </i>Inicialmente, é necessário
um grande esforço para que uma pessoa mantenha a atenção plena naquilo que
supostamente ela estiver fazendo, nem ela deve esperar que essa atenção plena
seja sempre agradável. Para escapar de uma tarefa ou situação tediosa ou que
não seja apreciada, tendemos a apelar para mundos de fantasia, esperanças ou
então para memórias, que são respectivamente as fugas delusas para o presente,
futuro ou passado. Mas para alguém que esteja realmente interessado em conhecer
a si mesmo, nenhuma dessas alternativas é muito recompensadora, já que elas são
compostas de delusão com vários ingredientes tais como medo, desejo e
ignorância. Enquanto que na prática estrita de meditação a atenção plena irá
seguir todas as perambulações da mente, na vida diária é melhor que a mente
retorne constantemente para a tarefa a ser realizada. A pessoa não deve
&quot;mandar&quot; a mente para nenhum lugar, nem para um mundo de sonhos nem
para o passado, nem para o futuro. O Buda comparou esses períodos de tempo da
seguinte forma:</p>

<p class=MsoNormal style='margin-top:5.0pt;margin-right:.5in;margin-bottom:
5.0pt;margin-left:.75in'>&quot;O passado é como um sonho,<br>
o futuro como uma miragem,<br>
enquanto que o presente é como as nuvens.&quot;<span style="mso-spacerun:
yes">   </span></p>

<p class=Normal>Esse símile pode ser útil ao ilustrar a mente que se movimenta rapidamente
entre sonhos, miragens e nuvens, todos sem substância, embora o presente em
constante mutação, tal como as nuvens no céu, seja o único aspecto do tempo que
pode ser comparado a coisas de maior realidade. A pessoa também pode considerar
a meditação como sendo o exercício de atenção plena que mantém a mente
&quot;dentro&quot; deste corpo, isto é, sempre focada em alguma das suas
facetas. É claro que somente os meditadores mais sinceros, que percebem nisso
uma vantagem maior do que qualquer prazer oferecido pelo mundo, é que
provavelmente praticarão dessa forma, já que isso corta não somente o interesse
por objetos externos mas também o entretenimento com idéias agradáveis e
intrigantes. </p>

<p class=Normal>De fato, com um trabalho que seja realmente interessante, o caminho da
atenção plena é o único meio de transformar o seu dia em algo que vale a pena.
Os dias passam e nos aproximam cada vez mais da morte e de um renascimento
desconhecido, enquanto que <i style='mso-bidi-font-style:normal'>agora </i>é
que existe a oportunidade de praticar o Dhamma. Ao invés de reagir com aversão
ou fantasias deludidas em relação ao que não se gosta (ou em outras
situações se entregando à cobiça), o Caminho da Atenção Plena constitui a
Prática do Caminho do Meio que transcende esses padrões arcaicos de reação. Não
existe necessidade de ser governado pela cobiça ou pela raiva, nem de ser
dominado pela delusão; mas somente a atenção plena pode mostrar o caminho para
superá-los. </p>

<p class=Normal>Trazer a mente constantemente de volta e soltá-la dos seus enredos é a
prática básica na vida do dia a dia. É sábio, também, aproveitar aqueles
momentos inesperados que surgem durante um trabalho, enquanto se está
aguardando para fazer algo, para encontrar alguém, ou esperando um ônibus ou
trem, ou a qualquer momento em que se esteja sozinho por alguns minutos. Ao
invés de pegar um jornal como distração, o celular ou uma outra pessoa para
fofocar, é mais produtivo fazer um &quot;retiro interior&quot;. Desligando a
atenção dos objetos exteriores, coloque a atenção na respiração, ou na
repetição de alguma frase do Dhamma, ou numa palavra importante tal como
&quot;Buddho&quot; ou &quot;Arahant,&quot; fazendo isso até que novamente tenha
que dar atenção à sua tarefa. Voltar para dentro de si sempre que possível será
muito benéfico, fortalecerá a prática sentada da mesma forma que, por sua vez,
fortalecerá a habilidade de fazer os retiros interiores. </p>

<p class=Normal>A atenção plena na respiração é particularmente boa como um método de
concentração para uso durante viagens e durante os períodos em que se está
esperando impacientemente por um ônibus ou trem. Porque ficar agitado ou
impaciente? Um pouco de atenção plena na respiração é a prática adequada para
esses momentos, já que ela acalma os movimentos exaltados da mente e os
movimentos inquietos do corpo. A pessoa não precisa fitar a paisagem sem
propósito enquanto viaja! Porque ser um escravo do &quot;domínio do olho&quot;
quando um pouco de prática proveitosa pode tomar o seu lugar? A pessoa não
precisa ouvir a conversa fútil dos outros, então porque ser um escravo do
&quot;domínio do ouvido&quot;? Não se pode fechar os ouvidos, mas qualquer um
pode de certa forma controlar a sua atenção ao praticar a atenção plena na
respiração. </p>

<p class=Normal>É a atenção plena, também, que auxilia a colocar o foco em contemplações
neutralizadoras. A luxúria, por exemplo, é rapidamente dissipada com a
imagem de um corpo em decomposição. Os olhares concedidos a lindas garotas
(ou homens bonitos) parecem ridículos quando se pensa que velhas senhoras e
igualmente homens anciãos nunca atraem esse tipo de atenção cheia de desejo.
Somente quando se vê como a cobiça incendeia aquele que a ela se entrega é que
parece valer a pena abandoná-la. </p>

<p class=Normal>Igualmente com a gula, que mesmo de uma forma suave, pode ser demolida ao
contemplarmos os processos corporais associados à comida. A comida mastigada
tem uma aparência muito menos palatável do que aquela que, ainda não misturada
com a saliva, é servida bem apresentada no prato. O vômito é exatamente a mesma
substância em processo de mudança, mas não estimula exatamente o desejo. O
excremento, mesmo se colocado no mais fino prato de ouro, não será atrativo -
no entanto, esse é o remanescente da comida que foi devorada com tanta avidez!
Quando houver contemplado a comida nesses três estágios, a cobiça terá
desaparecido completamente e a comida será tomada como se fosse um medicamento
para conservar o corpo. </p>

<p class=Normal>A atenção plena também é responsável por tornar-nos suficientemente
conscientes num momento de raiva para que dirijamos a mente para outros objetos
ou pessoas. É a atenção plena que nos adverte sobre uma situação em que a raiva
poderá surgir, e é ela que possibilita que evitemos essa situação e
permaneçamos com equanimidade, ou com amor bondade <i style='mso-bidi-font-style:
normal'>(metta)</i> quando as Moradas Divinas estiverem bem desenvolvidas. </p>

<p class=Normal>Quando a inveja mostra a sua cara feia, a atenção plena proporciona presença
de espírito para saber que &quot;a inveja surgiu&quot; e se o esforço para
despertar a alegria com a felicidade dos outros falhar, será a atenção plena
que irá ajudar a permanecer com equanimidade, ou se tudo falhar, ajudará a
desviar a atenção para outros objetos. </p>

<p class=Normal>O Buda verdadeiramente disse, &quot;A atenção plena, eu declaro, ajuda em
todas as situações.&quot;<span style="mso-spacerun: yes">  </span></p>

<p class=Normal>As implicações sociais da meditação deveriam ser óbvias pelo que foi dito acima.
Aqueles que possuem a delusão peculiar de que o Budismo é uma religião de
isolamento meditativo, que não oferece nenhum benefício social para a
sociedade, devem compreender que um Budista acredita que a sociedade só pode
ser mudada para melhor e com algum grau de permanência, começando o trabalho
consigo mesmo. Os ideais Budistas de sociedade estão expressos num grande
número de importantes discursos dirigidos pelo Buda a pessoas leigas e neles o
desenvolvimento do individuo é sempre enfatizado como um fator necessário. As
vantagens de uma sociedade em que existe um grande número de pessoas vivendo em
paz consigo mesmas não precisam ser enfatizadas. O desenvolvimento de sabedoria
e compaixão em uma pessoa tem o efeito de fermentar toda a 'massa de pão'
materialista que se encontra à sua volta. O chamado Budista, portanto, consiste
em<span style="mso-spacerun: yes">  </span><i style='mso-bidi-font-style:normal'>primeiro
</i>conquistar a paz no próprio coração, quando então surgirá,<i
style='mso-bidi-font-style:normal'> </i>muito naturalmente, a paz no mundo.
Tentar obter a paz pelo caminho contrário não será prático, nem irá produzir
uma paz duradoura, pois as raízes da cobiça, raiva e delusão ainda terão o
pulso firme sobre o coração das pessoas. Impraticável? Apenas para aqueles que
não praticam. Aqueles que se dedicam ao cultivo da atenção plena acabam
descobrindo por si mesmos como ela ajuda a solucionar os problemas da vida.</p>

<p class=Normal>Embora muitas viagens a lugares remotos e atrativos sejam agora realizadas
com facilidade, o caminho que conduz a Nibbana ainda requer esforço. Mas se o
caminho às vezes é sombrio tendo como única luz guia a atenção plena, pelo
menos através da reflexão saberemos que o objetivo é glorioso e de grande
valor, não somente para si mesmo mas para os outros também.<span
style='font-size:10.0pt;mso-bidi-font-size:12.0pt'> <o:p></o:p></span></p>


