<p class=Normal>As páginas que seguem, inicialmente, foram escritas para compor as novas
seções a serem adicionadas na segunda edição do meu livro introdutório sobre o
Dhamma, <i style='mso-bidi-font-style:normal'>O Que é o Budismo (What is
Buddhism?).</i> No entanto, como muitas pessoas possuem um grande interesse na
prática de meditação mas estão distantes das fontes onde se encontram as
tradições vivas, espero que o texto que segue produza benefícios, mesmo estando
separado das seções do livro às quais serve de complemento. </p>

<p class=Normal>Quando pensamos em meditação, nunca é demasiado enfatizar a importância de
termos as razões corretas ao adotarmos a sua prática. Meditação,ou, a melhor tradução
de <i style='mso-bidi-font-style:normal'>samadhi</i><span style='mso-bidi-font-style:
italic'> -</span><i style='mso-bidi-font-style:normal'> </i>concentração - é
somente um aspecto da prática Budista, e <i style='mso-bidi-font-style:normal'>deve,
</i>para ser bem sucedida, <i style='mso-bidi-font-style:normal'>andar de mãos
dadas </i>com outras práticas tais como a generosidade, nobreza, não violência,
paciência, contentamento e humildade. Se essas genuínas qualidades do Dhamma,
inicialmente, não existirem na pessoa, nem se desenvolverem através da prática,
então existe algo de muito errado e somente uma pessoa muito tola tentaria
prosseguir. A prática da concentração se apóia sobre uma sólida base de virtude
<i style='mso-bidi-font-style:normal'>(sila) </i>e não será bem sucedida se a
pessoa não fizer um<span style="mso-spacerun: yes">  </span>esforço verdadeiro
para<span style="mso-spacerun: yes">  </span>seguir os preceitos de maneira
rigorosa. Os sinais de &quot;progresso&quot; na concentração não são visões
estranhas, sensações peculiares ou outras coisas do gênero (embora seja concebível
que elas também possam surgir nos casos em que o progresso é realizado), mas,
particularmente, um crescimento abrangente e harmonioso no caminho do Dhamma.
Se alguém não possui um mestre, deve estar duplamente vigilante, pois de outra
forma, não terá consciência se alguma das distrações de Mara estiver prestes a
ter êxito, ou, se na verdade está avançando na prática do Caminho do Meio. </p>

<p class=Normal>Este ensaio é uma síntese de <i style='mso-bidi-font-style:normal'>Buddhism
Explained,</i> a segunda edição (revisada e ampliada) do livro anteriormente
conhecido como <i style='mso-bidi-font-style:normal'>What is Buddhism?</i>
publicado por The Social Science Press of Thailand, Phya Thai Road, Chula Soi
2, Bangkok. <span style='font-size:10.0pt;mso-bidi-font-size:12.0pt'><o:p></o:p></span></p>

