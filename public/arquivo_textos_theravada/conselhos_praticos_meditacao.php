34. equanimidade ..... {contrapõe a preocupação} </p>

<p class=Normal style='margin-top:5.0pt;margin-right:.5in;margin-bottom:
5.0pt;margin-left:.75in'>31 recomendado para personalidades <i style='mso-bidi-font-style:normal'>enraivecidas</i>
</p>

<p class=Normal><b>Quatro Estados sem forma <i style='mso-bidi-font-style:normal'>(arupa-bhava)<o:p></o:p></b></i></p>

<p class=Normal style='margin-top:5.0pt;margin-right:.5in;margin-bottom:
5.0pt;margin-left:.75in'>35. esfera do espaço infinito<br>
36. &quot; &quot; consciência infinita<br>
37. &quot; &quot; nada<br>
38. &quot; &quot; nem percepção, nem não percepção </p>

<p class=Normal>Estas absorções sem forma não podem ser desenvolvidas exceto se as quatro
absorções da forma já tiverem sido aperfeiçoadas. Diz-se que este grupo de
quatro pode ser explorado com base na quarta absorção <i style='mso-bidi-font-style:
normal'>(jhana).</i> Como existe a possibilidade de que muito poucas pessoas
experimentem isto, seguimos adiante para: </p>

<p class=Normal><b>Percepção do aspecto repulsivo da Comida</b> </p>

<p class=Normal style='margin-top:5.0pt;margin-right:.5in;margin-bottom:
5.0pt;margin-left:.75in'>39. Enquanto isto é essencial para o bhikkhu que depende de alimentos
esmolados (que algumas vezes são bons outras não), as pessoas leigas também
podem se beneficiar com esta prática, que Acariya Buddhaghosa observou ser para
personalidades <i style='mso-bidi-font-style:normal'>inteligentes, </i>e que
está projetada para diminuir e conduzir à destruição da cobiça e da gula. </p>

<p class=Normal><b>Definição dos Quatro Grandes Elementos</b></p>

<p class=Normal style='margin-top:5.0pt;margin-right:.5in;margin-bottom:
5.0pt;margin-left:.75in'>40. Eles são a terra (solidez), água (coesão), fogo (temperatura), e ar (movimento),
os quais caracterizam os nossos corpos físicos. Esses elementos podem ser
percebidos através de uma análise baseada no uso da atenção plena. Se diz que
esta prática é particularmente aconselhada para as personalidades <i
style='mso-bidi-font-style:normal'>inteligentes</i>. </p>

<p class=Normal>Aquelas práticas que não fazem menção a um tipo de personalidade são
adequadas para todas. Como todas essas práticas possuem como objetivo o
enfraquecimento e por fim a destruição das contaminações <i style='mso-bidi-font-style:
normal'>(kilesa),</i> a pessoa pode avaliar o quão importantes elas são
consideradas no treinamento Budista. Onde as contaminações são encontradas, ali
a penumbra da ignorância tem domínio, mas onde elas não são encontradas, ali
brilha a sabedoria e compaixão da Iluminação. </p>


