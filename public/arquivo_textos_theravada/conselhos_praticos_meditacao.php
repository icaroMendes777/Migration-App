<p class=Normal>&nbsp;</p>


<p class=Normal><a name=ch8></a><b>Perigos na Meditação</b> <a
href="#Top"><span style='text-decoration:none;text-underline:none'><img border=0 title="Topo" src="../img/tup.gif"></span></a>
</p> 

<p class=Normal>Enquanto a quantidade de formas que podem fazer com que um meditador se
desvie do caminho são muitas, as mencionadas abaixo merecem atenção especial
devido à sua ocorrência frequente. Primeiro, um perigo que nunca é demais
enfatizar é a falta da motivação correta para a prática de meditação. Ao descrever
o Nobre Caminho Óctuplo, na sua seção de &quot;sabedoria&quot;, imediatamente
em seguida ao entendimento (inicialmente) intelectual correto, vem a motivação
(ou pensamento) correta, dessa forma enfatizando que as raízes emocionais por
trás da prática do Caminho devem ser hábeis: aquelas conectadas com a renúncia
(ausência de cobiça), amor bondade (ausência de raiva) e não violência são
mencionadas. Se alguém começar a praticar a meditação Budista sem o
entendimento correto de <i style='mso-bidi-font-style:normal'>dukkha</i> e a
sua cessação e sem a motivação correta, então a meditação estará sujeita a
seguir por caminhos completamente errados. </p>

<p class=Normal>Existem aqueles, por exemplo, que iniciaram a prática de meditação como
forma de obter poderes, para que pudessem facilmente influenciar ou hipnotizar
os discípulos. Outros a viram como uma maneira rápida de obter tanto discípulos
como riquezas. A fama também pode ser um motivo torpe. Tudo isso tomado
como<span style="mso-spacerun: yes">  </span>motivação para a meditação, pode
facilmente conduzir aquele que é descuidado à enfermidade e às vezes ao desequilíbrio
mental. Não existe nada pior na meditação Budista, onde a própria experiência
pessoal é de suma importância, do que um discípulo cru que se apresenta como um
mestre. </p>

<p class=Normal>Isto obviamente conduz a um outro tipo de perigo - o orgulho, do qual
existem várias formas. Uma delas é o orgulho da pessoa que viu manifestações de
luz durante a meditação e supõe que esse seja um sinal que antecede a absorção
mental. Depois existe o orgulho daquele que toca uma absorção mental apenas por
um instante e como resultado assume que se tornou um dos Nobres e isso pode ser
um poderoso fator para convencer a si mesmo e até mesmo aos outros. Pessoas
comuns que se dedicam à meditação devem tomar cuidado com atitudes de exibição
de virtudes superiores: &quot;<i>Eu</i> faço o esforço, enquanto que você…&quot; ou,
&quot;<i style='mso-bidi-font-style:normal'>Eu </i>medito todos os dias,
enquanto que você…&quot; O orgulho é um grande obstáculo a qualquer progresso e
na medida que somente um Buda ou Arahant está totalmente livre disso, cada um
deveria ter a atenção plena para manter o orgulho sob controle.<span
style="mso-spacerun: yes">    </span></p>

<p class=Normal>Relacionado a isso, existe o perigo para aquela pessoa que sempre está à
procura do chamado progresso. Ela está segura que está fazendo
&quot;progresso&quot; porque na meditação ela vê luzes, ouve sons, ou sente
sensações estranhas. Ela se torna cada vez mais fascinada por essas ocorrências
à medida que o tempo passa e gradualmente se esquece de que começou com a
aspiração de encontrar o caminho para a Iluminação. A sua &quot;meditação&quot;
então degenera para visões e ocorrências estranhas, conduzindo-a para as
esferas do ocultismo e da magia. Não existe maneira mais segura do que esta
para um meditador ficar confuso. A despeito de que tais manifestações possam
ser fascinantes, elas devem ser eliminadas com vigor lançando-se mão da atenção
plena, nunca permitindo que o pensamento discursivo a seu respeito surja e
assim evitando essas distrações. </p>

<p class=Normal>Entre as &quot;visões&quot; que podem ocorrer, quer sejam internas
(produzidas pela própria mente) ou externas (produzidas por outros seres),
alguns meditadores podem ter uma experiência atemorizante, tal como uma visão
do próprio corpo reduzido a ossos ou inchado como um cadáver em decomposição.
Se ocorrer uma experiência dessas, ou outras semelhantes, a pessoa deve
arrancar a mente da visão imediatamente, na hipótese de que ela não possua um
mestre. Visões atemorizantes que algumas pessoa experimentam podem ser muito
úteis se utilizadas da forma correta, mas sem a orientação de um mestre elas
devem ser evitadas. </p>

<p class=Normal>Um outro perigo é tentar meditar enquanto a pessoa ainda está insegura
emocionalmente, desequilibrada ou imatura. Compreender o valor de ações
meritórias ou habilidosas pode ser muito útil nesses casos. Como o mérito
purifica a mente, é uma base excelente para o desenvolvimento da mente e,
ambos, a facilidade com que as absorções são alcançadas e a facilidade com que
o insight surge estão até certo ponto na dependência do mérito. Ações
meritórias não são difíceis de serem encontradas na vida. Elas são o núcleo da
boa vida Budista: donativos e generosidade, seguir os preceitos, ajudar e
prestar serviços aos outros, reverência, ouvir o Dhamma de todo coração, fazer
com que o entendimento do Dhamma seja correto, todas essas e muitas outras são
ações meritórias que trazem a felicidade e maturidade emocional. O <i
style='mso-bidi-font-style:normal'>Mérito,</i> a pessoa deve<span
style="mso-spacerun: yes">  </span>se lembrar sempre, <i style='mso-bidi-font-style:
normal'>abre portas em todos os lugares. </i>Cria possibilidades, gera
oportunidades. Ter uma mente que está todo o tempo focada em realizar mérito é
ter uma mente que poderá ser treinada no desenvolvimento das absorções e do
insight. </p>

<p class=Normal>Obviamente que tentar praticar a meditação e ao mesmo tempo continuar
mantendo os antigos desejos, gostos e desgostos, é, no mínimo, dificultar o
caminho, se não torná-lo perigoso. A meditação implica em renúncia, e nenhuma
prática terá êxito a não ser que a pessoa esteja, no mínimo, preparada para
fazer esforços para refrear a cobiça e a raiva, controlar a luxúria e entender
quando a delusão estiver obscurecendo o coração. Até que ponto a renúncia é
adotada e se isso envolve mudanças externas (tal como tornar-se um monge ou
monja), depende muito da pessoa e das suas circunstâncias, mas uma coisa é
certa: a renúncia interna, caracterizada pela atitude de desistência dos
eventos mentais inábeis e prejudiciais e da entrega aos prazeres corporais, são
absolutamente essenciais.<span style="mso-spacerun: yes">  </span></p>

<p class=Normal>Com freqüência, conectado aos perigos acima, existe um outro que é
encontrado quando uma pessoa, repentinamente, tem uma oportunidade para se
dedicar a um período mais longo de meditação. Ela senta com uma firme
resolução, &quot;Agora irei meditar&quot;, e apesar da sua energia nunca ter
estado tão boa e apesar de ela sentar e sentar, caminhar e caminhar, ainda
assim a sua mente continua perturbada e sem paz. Pode muito bem ser que o seu
intenso esforço tenha muito a ver com as suas distrações. Além disso, ela tem
que aprender que é necessário meditar conhecendo as limitações do seu caráter.
Da mesma forma como um trabalhador conhece os limites da sua força e toma
cuidado para não se exaurir, o mesmo cuidado possui o meditador habilidoso.
Através da atenção plena a pessoa deve saber quais são os extremos da preguiça
e do esforço que devem ser evitados.</p>

<p class=Normal>É provocando tensão ou forçando a prática da meditação que muitos estados
emocionais perturbados surgem. Explosões repentinas de raiva intensa por
questões insignificantes, desejos e luxúria intensos, delusões estranhas e
fantasias ainda mais peculiares podem ser produzidas pela prática árdua sem
inteligência. </p>

<p class=Normal>Com todos esses perigos, o que mais se necessita é um mestre para dar
conselhos, de forma que essas e outras direções erradas sejam evitadas e a
pessoa se mantenha no caminho para Nibbana. Aqueles que não possuem um mestre
deveriam prosseguir com o máximo de cautela, assegurando que o desenvolvimento
da atenção plena seja realmente muito bom. Se<span style="mso-spacerun: yes"> 
</span>tiverem atenção plena e virem que apesar dos seus esforços, a sua
prática de meditação não está produzindo nenhuma diferença efetiva nas suas
vidas em termos de uma maior paz<span style="mso-spacerun: yes"> 
</span>interior, ou externamente em relação aos outros, então é evidente que
algo está errado. A meditação pode ser deixada de lado durante algum tempo
enquanto se faz um esforço para contactar uma fonte de informação genuína, de
preferência um mestre de meditação, enquanto isso, deve-se atentar para
problemas morais não resolvidos, pois até que eles sejam solucionados não
permitirão que a mente se desenvolva; e fazer um grande esforço para viver de
acordo com os padrões Budistas. Quando assuntos tão básicos desse tipo são
negligenciados, a pessoa não pode esperar realizar muito progresso na prática do
Caminho do Meio.<span style='font-size:10.0pt;mso-bidi-font-size:12.0pt'> <o:p></o:p></span></p>


