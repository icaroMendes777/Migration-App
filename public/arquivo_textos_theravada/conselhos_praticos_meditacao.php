<p class=Normal><a name=ch9></a><b>Apêndice <a
href="#Top"><span style='text-decoration:none;text-underline:none'><img border=0 title="Topo" src="../img/tup.gif"></span></a>
<br>
40 Exercícios de Meditação<br>
conforme relacionado no <i style='mso-bidi-font-style:normal'>Caminho da
Purificação (Visuddhimagga)<o:p></o:p></b></i></p>

<p class=Normal>Se a pessoa não tem um mestre de meditação de quem possa solicitar um objeto
de meditação, então ela terá que contar com o seu próprio conhecimento sobre o
seu caráter para prescrever uma meditação adequada para si mesma. Existem
quarenta exercícios de meditação <i style='mso-bidi-font-style:normal'>(kammatthana)</i>
registrados pelo eminente mestre Buddhaghosa como sendo adequados para certos
tipos de caráter. Para efeitos de meditação, ele considera seis tipos de
caráter: fiéis, inteligentes e especulativos (aqueles em que as raízes hábeis e
benéficas da ausência de cobiça, ausência de raiva e não delusão são dominantes
de formas variadas); e cobiçosos, raivosos e deludidos (em que as raízes
inábeis e prejudiciais da cobiça, raiva e delusão são dominantes). Existem dois
problemas com essa classificação: primeiro, tipos &quot;puros&quot; são
raramente encontrados, sendo que a maioria das pessoas são mesclas de dois ou
mais tipos - e além disso uma mescla em constante mutação; e segundo, é
particularmente difícil julgar a qual classe pertence o caráter de uma pessoa
já que a sua delusão e o orgulho são capazes de nublar a sua capacidade de
julgamento. Esse é um assunto aparentemente sem importância no qual o valor de
um mestre de meditação pode ser enxergado com facilidade. No entanto, a pessoa
pode aprender muito sobre si mesma, ao estar plenamente atenta <i
style='mso-bidi-font-style:normal'>no momento em que algum evento inesperado
ocorra. Nesse momento </i>a pessoa pode identificar a sua reação e as
contaminações que estão presentes na mente. Julgamentos subseqüentes não valem
muito já que nesse momento a mente já estará convencida com as suas próprias
justificativas e outros tipos de distorções acerca do evento original.</p>

<p class=Normal>Abaixo encontra-se a lista dos quarenta exercícios de meditação com algumas
notas sobre a sua prática, os tipos de caráter que se beneficiam e os tipos de
contaminações que são combatidas por cada um. Os exercícios de meditação usados
com mais freqüência estão indicados com um asterisco (<b style='mso-bidi-font-weight:
normal'>*</b>). </p>

<p class=Normal><b>Dez Kasinas (esferas, lit: totalidades)</b></p>

<p class=Normal style='margin-top:5.0pt;margin-right:.5in;margin-bottom:
5.0pt;margin-left:.75in'>1. terra<br>
2. água<br>
3. fogo<br>
4. ar<br>
5. azul<br>
6. amarelo<br>
7. vermelho<br>
8. branco<br>
9. luz <b style='mso-bidi-font-weight:normal'>*</b><br>
10. Espaço limitado </p>

<p class=Normal style='margin-top:5.0pt;margin-right:.5in;margin-bottom:
5.0pt;margin-left:.75in'>5-8 recomendados para a prática de personalidades <i style='mso-bidi-font-style:
normal'>enraivecidas </i>devido às suas cores puras e agradáveis. </p>

<p class=Normal>Exceto pelos itens 5-8, nenhuma mácula moral em particular é contraposta por
estas dez kasinas. Como elas devem ser desenvolvidas através do olho, elas não
são adequadas para quem tenha a visão débil (de acordo com Buddhaghosa). </p>

<p class=Normal>A única das dez kasinas que parece ser muito praticada atualmente é a kasina
da luz, que algumas pessoas descobrem surgir com bastante naturalidade ao
começar concentrar a mente. Apesar de que as explicações de Acariya Buddhaghosa
no <i style='mso-bidi-font-style:normal'>Caminho da Purificação </i>tendem a
enfatizar a importância de se usar recursos externos para a prática (como fazer
a kasina da terra é descrito em grande detalhe), sempre que o autor ouviu sobre
o seu emprego (na Tailândia), elas foram sempre sob a forma de visões <i
style='mso-bidi-font-style:normal'>(nimitta)</i> que surgem internamente e são
desenvolvidas a partir dessa base. Parece que a contemplação de uma kasina
externa é desconhecida na Tailândia. </p>

<p class=Normal><b>Dez Tipos de Objetos Repulsivos <i >(asubha)         <o:p></o:p></b></i></p>

<p class=Normal style='margin-top:5.0pt;margin-right:.5in;margin-bottom:
5.0pt;margin-left:.75in'>11. O (cadáver) inchado contrapondo o deleite com a
beleza das proporções<br>
12. O lívido ... beleza das complexões<br>
13. O supurado ... odores e perfumes<br>
14. O esquartejado ... totalidade ou solidez<br>
15. O consumido ... corpo bem provido de carnes <br>
16. O disperso ... graça dos membros<br>
17. O picado e disperso ... graça do corpo como um todo<br>
18. O sangrento ... ornamentos e jóias<br>
19. O infestado pelos vermes ... posse do corpo<br>
20. O esqueleto ... ter ossos e dentes belos </p>

<p class=Normal style='margin-top:5.0pt;margin-right:.5in;margin-bottom:
5.0pt;margin-left:.75in'>11-20 recomendados para personalidades <i style='mso-bidi-font-style:normal'>cobiçosas</i>.<span
style="mso-spacerun: yes">                      </span></p>

<p class=Normal>Estas e outras listas semelhantes no <a href="../sutta/MN10.php">Satipatthana
Sutta</a> refletem uma época em que era comum se desfazer dos cadáveres em
cemitérios a céu aberto. Agora, no entanto, mesmo nos países Budistas eles são
difíceis de ser encontrados, sem falar nos países do Ocidente. Atualmente, na
Tailândia, os mestres enfatizam que o <i style='mso-bidi-font-style:normal'>próprio
corpo </i>deve ser visto dessas formas, como uma visão <i style='mso-bidi-font-style:
normal'>(nimitta),</i> durante o processo de desenvolvimento da mente. Como
essas visões podem ser atemorizantes, a pessoa deve ter a orientação de um
mestre hábil para lidar com tais visões, quando então elas poderão ser de
grande benefício. Deve ser enfatizado aqui que não existe nada de mórbido na
contemplação de visões como essas, interiores ou exteriores. A decomposição do
corpo é algo natural, mas normalmente não é visto assim porque as pessoas não
gostam de admitir isso. Ao invés de encarar a decomposição do corpo e mostrá-la
abertamente, os cadáveres são até mesmo preparados para parecerem atrativos por
embalsamadores e maquiadores e quando isso não pode ser feito, eles são
arrumados em bonitos caixões com flores resplandecentes, etc. O treinamento
Budista faz com que a pessoa olhe diretamente para esses aspectos da vida que
normalmente (isto é, com cobiça) não são considerados &quot;bons&quot;, e faz com
que a pessoa calmamente os encare em relação à sua própria mente e corpo. </p>

<p class=Normal><a name=anussati></a><b>Dez Recordações <i >(anussati)  <o:p></o:p></b></i></p>

<p class=MsoNormal style='margin-top:5.0pt;margin-right:.5in;margin-bottom:
5.0pt;margin-left:.75in'>21. sobre o Buda <b style='mso-bidi-font-weight:normal'>*</b><br>
22. &quot; o Dhamma<br>
23. &quot; a Sangha<br>
24. &quot; virtude <i style='mso-bidi-font-style:normal'>(sila)</i> .....
{contrapõe a contaminação <i style='mso-bidi-font-style:normal'>(kilesa)</i> da
má conduta <i style='mso-bidi-font-style:normal'>(duccarita)</i>}<br>
25. &quot; generosidade ..... {contrapõe a mesquinhez <i style='mso-bidi-font-style:
normal'>(macchariya)</i>}<br>
26. &quot; celestiais ..... {contrapõe a dúvida <i style='mso-bidi-font-style:
normal'>(vicikiccha)</i>}<br>
27. &quot; morte ..... {contrapõe a preguiça}<br>
28. &quot; corpo <b style='mso-bidi-font-weight:normal'>*</b> ..... {contrapõe
a cobiça e a sensualidade <i style='mso-bidi-font-style:normal'>(kama-raga)</i>}<br>
29. &quot; respiração <b style='mso-bidi-font-weight:normal'>*</b> .....
{contrapõe a delusão, preocupação}<br>
30. &quot; paz ..... {contrapõe a perturbação} </p>

<p class=Normal style='margin-top:5.0pt;margin-right:.5in;margin-bottom:
5.0pt;margin-left:.75in'>21-26 recomendado para os tipos de personalidade
baseadas na <i style='mso-bidi-font-style:normal'>fé</i><br>
27 &quot; &quot; personalidades <i style='mso-bidi-font-style:normal'>inteligentes</i>
<br>
28 &quot; &quot; personalidades<i style='mso-bidi-font-style:normal'>
cobiçosas<span style="mso-spacerun:
yes">                                                              </span></i><br>
29 &quot; &quot; personalidades<i style='mso-bidi-font-style:normal'>
deludidas/especulativas<span style="mso-spacerun:
yes">                                   </span></i><br>
30 &quot; &quot; personalidades <i style='mso-bidi-font-style:normal'>inteligentes<o:p></o:p></i></p>

<p class=Normal>Este grupo de dez possui características mais variadas do que os dois grupos
anteriores. Ao praticar as três primeiras recordações (21-23) a pessoa recita
as listas das qualidades de cada um. Ou, se a mente não ficar concentrada dessa
forma, a pessoa escolhe uma qualidade em particular para recitá-la
silenciosamente e continuamente (tal como &quot;Buddho&quot; ou
&quot;Araham&quot;). Rosários são usados em alguns lugares em conexão com este
tipo de prática. As recordações sobre virtude e generosidade são
particularmente boas de serem cultivadas durante a velhice. A pessoa revê todas
as ações meritórias <i style='mso-bidi-font-style:normal'>(puñña)</i>
praticadas ao longo da vida e ao recordá-las a mente fica tranqüila e satisfeita,
e tendo esse estado mental no momento da morte irá assegurar um renascimento em
condições muito favoráveis. A menos que tenha visto algum, a pessoa somente
poderá refletir sobre seres celestiais <i style='mso-bidi-font-style:normal'>(deva)</i>
baseado na sua fama. Este tipo de prática é adequada para aquelas pessoas que
incrementaram o alcance das suas mentes e dessa forma fizeram contato com
outros seres mais sutis. A recordação sobre a morte pode ser feita por
personalidades <i style='mso-bidi-font-style:normal'>inteligentes </i>já que
elas não ficarão atemorizadas pelas possibilidades que este tipo de prática
revela. É um grande incentivo praticar <i style='mso-bidi-font-style:normal'>agora
</i>sabendo que não existe certeza se daqui a um segundo a pessoa ainda estará
viva. A recordação vinte oito - sobre o corpo - é para personalidades <i
style='mso-bidi-font-style:normal'>cobiçosas, </i>que precisam desenvolver
desapego em relação ao corpo. Isto é alcançado através da análise do corpo em
trinta e duas partes desprovidas de beleza e depois através da seleção de uma
ou mais dessas partes e o seu exame. No entanto, esta prática atinge a sua
perfeição quando através do insight o corpo é iluminado e os seus vários
componentes são vistos com clareza e a sua natureza é compreendida. A atenção
plena na respiração é recomendada para acalmar e aclarar a mente e pessoas de
quase todos os tipos de temperamento podem praticá-la com benefício, embora
muito cuidado seja necessário nos níveis mais sutis desse exercício. A
respiração nunca é forçada mas observada constantemente com atenção plena,
sendo que o ponto para a concentração em geral é a ponta do nariz ou narinas.
No entanto existem variações entre mestres distintos. A recordação sobre a
paz, diz o grande Acariya, somente beneficia quem já experimentou Nibbana, tal
como aqueles que já entraram na correnteza (<i style='mso-bidi-font-style:normal'>sotapanna</i>);
mas outras pessoas podem obter alguma calma através da recordação da
tranqüilidade. A paz mencionada nesse caso é na verdade Nibbana, e como uma
pessoa não pode se recordar daquilo que não conheceu, que é o caso de uma pessoa
mundana <i style='mso-bidi-font-style:normal'>(puthujjana),</i> esta é uma
prática para os Nobres <i style='mso-bidi-font-style:normal'>(ariya).</i> </p>

