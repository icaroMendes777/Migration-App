<p class=Normal><a name=ch5></a><b>A Paciência e seu Aperfeiçoamento<br>

<i>(Khanti-parami)</i></b> <a
href="#Top"><span style='text-decoration:none;text-underline:none'><img border=0 title="Topo" src="../img/tup.gif"></span></a>
</p>

<p class=Normal>A
paciência é uma excelente qualidade, muito elogiada nas escrituras Budistas.
Ela pode ser desenvolvida com facilidade apenas se a inquietação e a raiva já
estiverem subjugadas na mente, tal como é feito na prática de meditação. A
impaciência, que tem a tendência de fazer com que a pessoa se apresse muito e
por isso perca muitas oportunidades boas, resulta da inabilidade em ficar
parado e permitir que as coisas se resolvam por si mesmas - o que algumas vezes
pode ocorrer sem que alguém tenha que intervir. A pessoa paciente acaba obtendo
muitas coisas de graça que não são obtidas pela pessoa empreendedora. Uma delas
é uma mente tranqüila, pois a impaciência agita a mente e traz consigo as
conhecidas enfermidades decorrentes da ansiedade do mundo moderno dos negócios.
A paciência tolera em silêncio - é essa qualidade que faz com que ela seja tão
valiosa no treinamento mental e particularmente na meditação. Não há motivo
para esperar a iluminação instantânea depois de cinco minutos de prática. O
café pode ser instantâneo, mas a meditação não é e só haverá dano ao tentar
apressá-la. Ao longo dos anos o lixo foi se acumulando, uma pilha enorme de
lixo mental, e então quando alguém começa a removê-lo com uma pequena colher de
chá, com que rapidez pode-se esperar que ele irá desaparecer? A paciência é a
resposta acompanhada pela energia decidida. O meditador paciente realmente
obtém<span style="mso-spacerun: yes">  </span>resultados duradouros; aquele que
busca &quot;métodos rápidos&quot; ou &quot;iluminação instantânea&quot; está
condenado por sua própria atitude a um grande desapontamento. De fato, em pouco
tempo deve ficar claro para qualquer um que investigue o Dhamma, que esses
ensinamentos não servem para as pessoas impacientes. Um Budista enxerga a sua
vida atual como algo breve, talvez com uma duração de oitenta anos, mais ou
menos, e a última das inúmeras vidas vividas até agora. Tendo isto em mente,
ele determina fazer o máximo que possa nesta vida para alcançar a Iluminação.
Mas ele não superestima a sua capacidade; ele calmamente e pacientemente segue <i
style='mso-bidi-font-style:normal'>vivendo </i>o Dhamma no dia a dia.
Precipitar-se em direção à Iluminação (ou àquilo que a pessoa pensa ser), é
como um touro em uma loja de cristais, ele não irá fazer muito progresso, isto
é, exceto se a pessoa for de uma natureza excepcional que seja capaz de
suportar esse tipo de tratamento e, o mais importante, que seja dedicada a um
mestre de meditação bastante habilidoso. Com paciência a pessoa não irá se
magoar, mas irá seguir pelo caminho de maneira cuidadosa, passo a passo.
Aprendemos que um Bodisatva está bem consciente disso e que ele cultiva a sua
mente com essa perfeição de forma que ele não seja perturbado por nenhuma das
ocorrências desagradáveis comuns neste mundo. Ele decide que será paciente com
as condições externas - não se perturbando se o sol estiver demasiado quente ou
o clima demasiado frio. Ele não ficará agitado se outros seres atacarem o seu
corpo, tal como insetos e mosquitos. Nem ficará perturbado quando as pessoas
disserem palavras grosseiras, mentiras ou insultos a seu respeito, quer seja
diretamente ou pelas costas. A sua paciência não se abala nem mesmo quando o
seu corpo for submetido ao sofrimento, golpes, paus e pedras, torturas e até
mesmo a própria morte; ele irá suportar tudo isso firmemente, tão inabalável é
a sua paciência. Aos monges Budistas se recomenda praticar da mesma forma. <o:p></o:p></span></p>

<p class=Normal>Na
tradição Budista a perfeição da paciência é particularmente mais conhecida do
que as outras. Isso se deve ao exemplo dado em uma extraordinária História de
Nascimento (Jataka). A História do Nascimento de Khantivadi (O Mestre da
Paciência)[<a href="#n2">2</a>] <a name=r2></a>deve ser lida com freqüência e
ser objeto de profunda e freqüente reflexão. Somente uma pessoa
excepcionalmente nobre, neste caso Gotama, em uma das suas vidas anteriores,
quando ele era chamado de o Mestre da Paciência, chegou a exortar um monarca
enraivecido e bêbado que devido à sua raiva enciumada vagarosamente
esquartejava o seu corpo. O Bodisatva possuía esse tipo de nobreza e essa
nobreza, tolerância constante e benevolência, é exigida de todos que tentam
alcançar o objetivo da Iluminação</span>.<span style="mso-spacerun: yes">  
</span><span style='font-size:12.0pt;mso-bidi-font-size:13.5pt;font-weight:
normal'><o:p></o:p></span></h3>


<p class=Normal>&nbsp;</p>

<hr size=2 width="100%" align=center>

<p class=Normal>&nbsp;</p>


<p class=Normal><a name=ch6></a><b>A
Energia e o seu Aperfeiçoamento<br>

<i>(Viriya-parami) </i></b> <a
href="#Top"><span style='text-decoration:none;text-underline:none'><img border=0 title="Topo" src="../img/tup.gif"></span></a>
</p>

<p class=Normal>Assim como é inconcebível a Iluminação se a pessoa não tiver paciência, a
Iluminação também<span style="mso-spacerun: yes">  </span>não pode ser
alcançada sem esforço. O Dhamma nunca encoraja a doutrina do fatalismo e os
verdadeiros Budistas nunca pensam que os eventos sejam rigidamente
predeterminados. Esse tipo de fatalismo é combatido pela própria atenção plena
e pela própria energia. Esta perfeição é a contrapartida da anterior e é
equilibrada pela prática, ambas asseguram que o Budista sincero nem aceitará passivamente
aquilo que ele deve combater, nem se precipitará<span style="mso-spacerun:
yes">  </span>perturbando a si mesmo e aos outros quando ele deveria ter
paciência. Deveria ser mencionado aqui, como aviso, que no mundo Budista podem
ser encontrados um grande número de &quot;métodos&quot; que aparentam prometer
as riquezas do Dhamma num piscar de olhos. A pessoa ouve comentários do tipo,
&quot;Qual a utilidade de livros e estudo?&quot; Ou mesmo, &quot;O
desenvolvimento da concentração é uma perda de tempo! A pessoa deveria apenas
praticar vipassana.&quot; Esse tipo de abordagem tendenciosa não reflete a
sabedoria do Buda, que sempre ensinava a necessidade de um desenvolvimento <i
style='mso-bidi-font-style:normal'>equilibrado </i>da mente. Livros e o seu
estudo <i style='mso-bidi-font-style:normal'>são </i>úteis para algumas pessoas
que desejam obter uma boa base daquilo que o Buda realmente disse, antes de
adotar uma prática mais intensiva. Quanto à outra afirmação, nenhum insight
verdadeiro (apenas idéias deludidas) irão surgir na pessoa cuja mente não
experimenta a concentração. Idéias como essas, que usualmente estão baseadas
em alguma experiência peculiar dos &quot;mestres&quot; que as originam, são
capazes de enganar a muitos, já que o desejo por resultados rápidos, combinado
com a antipatia pelo trabalho duro necessário, são facilmente estimulados. Tem
que haver paciência para aceitar que as condições requeridas para obter sucesso
na meditação (tal como descrito aqui) têm que ser satisfeitas e que o único
resultado, se assim não for feito, será desviar-se do Caminho. O meditador se
dedica com firmeza a qualquer tarefa que tenha que desempenhar e ao concluí-la,
não se sente cansado mas imediatamente toma um novo objetivo.<span
style="mso-spacerun: yes">    </span></p>

<p class=Normal>Com respeito a isso, é interessante que há dois tipos de cansaço: aquele
relativo à exaustão física e o outro tipo que é induzido mentalmente e que
envolve os fatores inábeis e prejudiciais da preguiça e do torpor. Enquanto que
o primeiro é obviamente inevitável, o último ocorre apenas quando a raiz inábil
e prejudicial da delusão (ou embotamento) se torna predominante na mente. Isso
ocorre quando há uma situação que é desagradável para &quot;mim&quot;, não
desejada e da qual &quot;eu&quot; quero escapar. As pessoas se queixam que
ficam muito mais cansadas ao praticar meditação sentada intensamente do que
quando por exemplo se dedicam a uma leitura pesada. Quando o eu se sente
ameaçado por um evento que poderá revelá-lo, então esse eu, enraizado na
ignorância, cria uma densa névoa de torpor que tem sua origem na delusão. Por
outro lado, muitos que têm praticado muita meditação observam que já não
precisam dormir tanto quanto antes, enquanto que a energia, quando se torna uma
perfeição tal como praticada pelo Bodisatva, é totalmente natural e não
forçada.</p>

<p class=Normal>Esta perfeição é ilustrada pela história do líder de uma caravana que salvou
os mercadores, homens e animais confiados ao seus cuidados, através da ação
vigorosa. Quando outros teriam se entregado à morte, já que a caravana havia
tomado um caminho errado no deserto e todos os suprimentos estavam exauridos, o
líder forçou um deles a que cavasse em busca de água, que foi encontrada. Dessa
forma, em uma vida passada Gotama, como o líder da caravana, fez esforço não
somente pela sua própria vida mas também pelo bem estar dos outros. Os monges
também são chamados de &quot;líderes de caravana&quot; em muitos textos das
escrituras em Pali, mostrando que não é somente o Buda ou um Bodisatva que é
capaz de guiar outros. Se conduzirmos o nosso treinamento com energia então nós
também teremos energia para o progresso dos outros. Muitas outras histórias como
essa podem ser encontradas nos textos Budistas mostrando o quão necessária é a
energia, da qual nascem a persistência e a determinação para ver aquilo que é
verdadeiramente real, Nibbana. </p>


<p class=Normal>&nbsp;</p>

