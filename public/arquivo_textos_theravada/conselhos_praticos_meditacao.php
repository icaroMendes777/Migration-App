<p class=Normal><a name=ch4></a><b>As Moradas Divinas e o seu Aperfeiçoamento<br>
<i style='mso-bidi-font-style:normal'>(Brahma-vihara)</b><o:p></o:p></i> <a
href="#Top"><span style='text-decoration:none;text-underline:none'><img border=0 title="Topo" src="../img/tup.gif"></span></a>
</p>

<p class=Normal>O
caminho em direção ao objetivo passa pelo que chamamos de Moradas Divinas, onde
as emoções profundamente enraizadas são transformadas de inábeis e prejudiciais
em hábeis e benéficas de acordo com o Dhamma. Como já foi enfatizado acima, o
objetivo de uma pessoa, bem como de todos os seres, é conquistar condições que
produzam a felicidade. Portanto, a pessoa deve agir de tal forma que a
felicidade resulte das suas ações. A pessoa deveria, nesse caso, tratar os
outros da maneira como ela gostaria de ser tratada, pois cada ser vivo tem muita
estima por si mesmo e deseja o seu próprio bem-estar e felicidade. A pessoa não
pode esperar ter uma felicidade isolada que surja sem uma causa ou que não
surja de si mesma, nem a felicidade pode ser esperada se alguém maltrata outros
seres, humanos ou não. Todos os seres desejam a vida e temem a morte, sendo
isso verdadeiro tanto para nós como para as outras criaturas. <o:p></o:p></span></h3>

<p class=Normal>Somente uma pessoa, que conduz a sua vida com dignidade e compaixão
constantemente, tem realmente estima por si mesma, pois ela pratica ações que
produzem grandes benefícios, grande felicidade. Outras pessoas, embora pensem
que têm estima por si mesmas, são na verdade os seus piores inimigos, pois elas
fazem para si mesmas aquilo que apenas um inimigo lhes desejaria. </p>

<p class=Normal>A boa conduta depende de uma mente bem treinada que gradualmente se libertou
das garras da cobiça, da raiva e da delusão. Ter tanta estima pelo próximo
quanto verdadeiramente se tem por si mesmo é fácil de falar mas difícil de
fazer. É um mérito particular dos ensinamentos do Buda sempre indicar <i
style='mso-bidi-font-style:normal'>como </i>um método deve ser traduzido em
experiência, o método neste caso é o treinamento da mente nas Moradas Divinas.
Quando mencionamos &quot;mente&quot;, essa palavra deve ser compreendida não no
sentido restrito dos processos intelectuais, mas, particularmente, ela deve
incluir o escopo completo da mente-coração, intelecto e emoções. </p>

<p class=Normal>Existem quatro estados que pertencem às Moradas Divinas: amor bondade,
compaixão, alegria altruísta e equanimidade. Esses, e especialmente o primeiro,
são objetos de meditação muito populares nos países Budistas. O que segue é uma
breve explicação de cada um. </p>

<p class=Normal><i style='mso-bidi-font-style:normal'>Amor bondade</i> <i style='mso-bidi-font-style:
normal'>(metta)</i> é um amor altruísta estendido a todos. Isto se torna fácil
quando as absorções <i style='mso-bidi-font-style:normal'>(jhanas) </i><span
style='mso-bidi-font-style:italic'>são conquistadas</span> e só então a
qualidade do amor bondade se torna uma parte integral do caráter da pessoa. Da
forma como as coisas normalmente são, as pessoas apenas &quot;amam&quot;
aquelas poucas pessoas às quais estão especialmente ligadas por laços de
família, etc. Esse é o tipo de amor com apego sensual, um amor limitado, e
aqueles que se encontram fora desse amor ou são ignorados ou não se gosta
deles. O amor sensual, portanto, não somente está ligado ao apego (cobiça) mas
também à raiva e à delusão, de forma que a pessoa que está satisfeita com esse
amor paga um alto preço por ele. Um amor sem apego dificilmente pode ser
concebido por muitas pessoas, mas esse tipo de amor é muito superior ao
anterior, não tendo apego ele pode se tornar infinito e não precisa estar
confinado a este ou aquele grupo de seres. Como ele pode se tornar infinito,
sem deixar ninguém de fora, não surge a questão das três raízes inábeis e
prejudiciais estarem vinculadas a ele. </p>

<p class=Normal>O amor bondade pode ser desenvolvido gradualmente a cada dia como parte da
prática de meditação, <i style='mso-bidi-font-style:normal'>mas para ser realmente
eficaz </i>ele precisa se mostrar na vida diária da pessoa. Ele torna a vida mais
fácil transformando as pessoas que antes eram antipáticas e odiadas em
indiferentes, inicialmente, mas depois, à medida que a prática vai se
fortalecendo, em objetos para o surgimento do amor bondade. É o remédio do Buda
para a enfermidade da raiva e da antipatia. Finalmente, o amor bondade tem dois
inimigos: o&quot;próximo&quot;, que é o apego sensual, com freqüência denominado
de &quot;amor&quot; de forma incorreta, enquanto que o inimigo
&quot;distante&quot; do seu desenvolvimento é a má vontade. No desenvolvimento do
amor bondade é necessário tomar cuidado com os dois.</p>

<p class=Normal><i style='mso-bidi-font-style:normal'>Compaixão</i> <i style='mso-bidi-font-style:
normal'>(karuna)</i> é sensibilizar-se em relação aos sofrimentos de outros
seres no mundo. A compaixão supera a indiferença insensível para com os infortúnios
dos seres que sofrem, humanos ou não. Da mesma forma, mostra-se na vida diária
de uma pesssoa através da vontade de sair da sua rotina para proporcionar ajuda
aonde for possível e de auxiliar aqueles que se encontram em aflição. Tem a
vantagem de reduzir o próprio egoísmo pela compreensão dos sofrimentos dos
outros. É o remédio do Buda para a crueldade, pois como alguém pode causar dano
aos outros tendo visto o quanto eles estão sofrendo? A compaixão também tem
dois inimigos: o &quot;próximo&quot; que é a simples pena ou dó; enquanto que o
inimigo &quot;distante&quot; é a crueldade.<span style="mso-spacerun: yes"> 
</span></p>

<p class=Normal><i style='mso-bidi-font-style:normal'>Alegria altruísta</i> <i
style='mso-bidi-font-style:normal'>(mudita)</i> é alegrar-se com os outros
pelos seus êxitos, ganhos e felicidade. A alegria altruísta supera a atitude de
rancor em relação aos outros e a inveja que pode surgir ao saber da alegria dos
outros. Mostra-se na vida de uma pessoa como uma alegria espontânea no momento
exato em que ela toma conhecimento de que outras pessoas obtiveram algum tipo
de ganho, material ou imaterial. Tem a vantagem de fazer com que a pessoa seja
franca com as demais e elimina a dissimulação. Uma pessoa que desenvolve a
alegria altruísta atrai muitos amigos que lhe são devotados e com eles e os
demais ela vive em harmonia. É o remédio do Buda para a inveja e o ciúme que
podem ser completamente inibidos. Os dois inimigos da alegria altruísta são a
simples alegria pessoal ao refletir sobre os próprios ganhos - esse é o inimigo
&quot;próximo&quot;; enquanto que o &quot;distante&quot; é a aversão ou o
descontentamento. </p>

<p class=Normal><i style='mso-bidi-font-style:normal'>Equanimidade</i> <i style='mso-bidi-font-style:
normal'>(upekkha)</i> deve ser desenvolvida para lidarmos com situações
difíceis cuja mudança, deveríamos admitir,<span style="mso-spacerun: yes"> 
</span>está acima das nossas forças. A equanimidade supera a preocupação e a
distração inúteis em relação a assuntos que, ou não dizem respeito à pessoa, ou
não podem ser mudados por ela. Deve aparecer na vida diária da pessoa como uma
habilidade para enfrentar situações difíceis com tranqüilidade e paz mental
imperturbável. A vantagem no seu desenvolvimento é que simplifica a vida com o
desligamento de atividades inúteis. É o medicamento do Buda para a distração e
a preocupação e os seus inimigos são a pura indiferença, que é o inimigo
&quot;próximo&quot;; enquanto que a cobiça e o seu parceiro ressentimento, que
envolvem a pessoa de maneira inábil em tantos assuntos, são os seus inimigos
&quot;distantes&quot;.<span style="mso-spacerun: yes">  </span></p>

<p class=Normal>A mente que praticou bem essas quatro virtudes e depois treinou bem o seu
uso na vida diária, já conquistou muito. </p>

<p class=Normal>Três das perfeições <i style='mso-bidi-font-style:normal'>(parami),</i> ou
qualidades, praticadas por muitos Budistas que aspiram à Iluminação também
podem ser esboçadas a seguir, já que elas também possuem uma influência
profunda na prática da meditação.</p>


