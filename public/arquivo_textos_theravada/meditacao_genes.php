<p class=Normal>Com cada vez mais evidências de que a meditação pode ter efeitos benéficos para a saúde, os cientistas têm buscado entender como essas práticas afetam fisicamente o corpo. </p>

<p class=Normal>Um novo estudo realizado por pesquisadores em Wisconsin, Espanha e França relata a primeira evidência de alterações moleculares específicas no corpo após um período de meditação da atenção plena (<i>vipassana</i>).</p>

<p class=Normal>O estudo investigou os efeitos de um dia de prática intensiva de <i>vipassana</i> em um grupo de meditadores experientes, comparados a um grupo de indivíduos controle não treinados que se envolveram em atividades tranquilas não-meditativas. Após oito horas de prática <i>vipassana</i>, os meditadores mostraram uma gama de mudanças genéticas e moleculares, incluindo níveis alterados nos mecanismos de regulação dos genes e níveis reduzidos de genes pró-inflamatórias, que por sua vez se correlacionam com uma recuperação física mais rápida de uma situação estressante. </p>

<p class=Normal>"Baseado no que conhecemos, este é o primeiro trabalho que mostra alterações rápidas na expressão de genes associados à prática da meditação vipassana", diz o autor do estudo, Richard J. Davidson, fundador do Center for Investigating Healthy Minds e William James and Vilas Professor de Psicologia e Psiquiatria da Universidade de Wisconsin- Madison. </p>

<p class=Normal>"O mais interessante, é que as mudanças foram observadas em genes que atualmente são os alvos de medicamentos anti-inflamatórios e analgésicos", diz Perla Kaliman, primeira autora do artigo e pesquisadora do Instituto de Investigaciones Biomédicas de Barcelona, Espanha ( CSIC - IIBB - IDIBAPS ), onde foram realizadas as análises moleculares.

<p class=Normal><a name=R1></a>O estudo foi publicado na revista Psychoneuroendocrinology. [<a href="#N1">1</a>]       </p>

<p class=Normal><a name=R1></a>Em estudos clínicos anteriores o treinamento baseado em <i>Vipassana</i> tem demonstrado efeitos benéficos nas doenças inflamatórias sendo endossado pela American Heart Association como um tratamento preventivo. Os novos resultados proporcionam um possível mecanismo biológico para esses efeitos terapêuticos. </p>

<p class=Normal>Os resultados mostram uma diminuição da regulação de genes que têm sido implicados nos processos inflamatórios. Os genes afetados incluem os genes pró-inflamatórios RIPK2 e COX2, bem como vários genes histona deacetilase (HDAC) que regulam epigeneticamente a atividade de outros genes através da remoção de um tipo de etiqueta química. Além do mais, o grau em que alguns desses genes foram reprimidos foi associado à recuperação mais rápida do cortisol num teste de estresse social envolvendo um discurso de improviso e tarefas que exigiram cálculos mentais realizadas em frente a uma platéia e uma câmera de vídeo. </p>

<p class=Normal>Talvez surpreendentemente, dizem os pesquisadores, não houve diferença nos genes testados entre os dois grupos de pessoas no início do estudo. Os efeitos observados foram vistos apenas nos meditadores depois da prática de <i>Vipassana</i>. Além disso, vários outros genes modificadores de DNA não mostraram diferenças entre os grupos, sugerindo que a prática de <i>Vipassana</i> afetou especificamente certas vias regulatórias. </p>

<p class=Normal>No entanto, é importante observar que o estudo não foi concebido para distinguir os efeitos naqueles que praticam o treinamento de meditação a longo prazo, daqueles com apenas um único dia de prática. Em vez disso, o resultado principal é que os meditadores experimentaram mudanças genéticas depois da prática de <i>Vipassana</i> que não foram observadas no grupo de não-meditadores - um resultado que comprova o princípio de que a prática de <i>Vipassana</i> pode levar a alterações epigenéticas do genoma. </p>

<p class=Normal>Estudos anteriores em roedores e em pessoas têm demonstrado respostas epigenéticas dinâmicas a estímulos físicos, tais como estresse, dieta ou exercício dentro de apenas algumas horas. </p>

<p class=Normal>"Nossos genes são bastante dinâmicos na sua expressão e estes resultados sugerem que uma mente calma pode realmente ter uma influência potencial sobre a expressão dos genes", diz Davidson. </p>

<p class=Normal>"A regulação de HDAC e dos processos inflamatórios podem representar alguns dos mecanismos que justificam o potencial terapêutico dos tratamentos baseados em <i>Vipassana</i>", diz Kaliman. "Nossas descobertas estabelecem as bases para futuros estudos para avaliar melhor as estratégias de meditação para o tratamento de condições inflamatórias crônicas."</p>

