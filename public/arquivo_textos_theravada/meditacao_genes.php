<p class=Normal>Há muito tempo os cientistas se perguntam por que a meditação tem tantos efeitos positivos.</p>

<p class=Normal>Depois de vencerem muitos preconceitos de seus colegas, cientistas já demonstraram que, entre os benefícios da meditação, estão por exemplo a redução do risco de ataques cardíacos e os derrames.</p>

<p class=Normal>A novidade agora é que se descobriu que a meditação altera a expressão de genes envolvidos com vários processos benéficos à saúde.</p>

<p class=Normal>Estudos anteriores já documentaram <a href="cerebro.php">mudanças no cérebro</a> quando as pessoas praticam meditação, mas esta é a primeira vez que se demonstra mudanças na expressão dos genes.</p>

<p class=Normal>Segundos os pesquisadores, esse pode ser o mecanismo principal que poderia explicar os efeitos benéficos relatados da meditação.</p>

<p class=Normal><b>Efeitos genéticos da meditação</b></p>

<p class=Normal>Herbert Benson e seus colegas do Massachusetts General Hospital em Boston analisaram os perfis genéticos de 26 voluntários - nenhum dos quais meditava regularmente - antes de ensinar-lhes uma técnica de relaxamento com duração de 10 a 20 minutos. A prática incluía recitar palavras, fazer exercícios de respiração e tentativas de interromper o fluxo rotineiro de pensamentos.</p>

<p class=Normal>Depois de oito semanas de meditação diária, o perfil genético dos voluntários foi analisado novamente. Grupos de importantes genes benéficos se tornaram mais ativos e os prejudiciais menos.</p>

<p class=Normal>Os genes ativados têm três principais efeitos benéficos: melhorar a eficiência das mitocôndrias, a usina de força das células, aumentar a produção de insulina, o que melhora o controle de açúcar no sangue, e evitar o esgotamento dos telômeros, as tampas dos cromossomos que ajudam a manter estável o DNA e assim evitar que as células se desgastem - em duas palavras, retardam o envelhecimento.</p>

<p class=Normal>Os genes que se tornaram menos ativos foram aqueles governados por um gene mestre chamado NF-kappaB, que desencadeia uma inflamação crônica que leva a doenças como a hipertensão arterial, doenças cardíacas, doença inflamatória intestinal e alguns tipos de câncer.</p>

<p class=Normal>Tomando amostras de sangue imediatamente antes e depois, após a realização da técnica num único dia, os investigadores também demonstraram que as alterações dos genes ocorreram em poucos minutos.</p>

<p class=Normal>Para efeito de comparação, os pesquisadores também coletaram amostras de 26 voluntários que praticavam durante pelo menos três anos as técnicas de relaxamento. Eles apresentavam os perfis dos genes benéficos, mesmo antes de executar suas rotinas no laboratório, o que sugere que as técnicas resultaram em mudanças a longo prazo nos seus genes.</p>

<p class=Normal>"Parece fazer sentido que essas respostas sejam observadas depois de apenas 15 a 20 minutos, assim como, inversamente, curtos períodos de estresse elevam os hormônios do estresse e geram outros efeitos fisiológicos que são prejudiciais a longo prazo," comentou Julie Brefczynski-Lewis, da West Virginia University em Morgantown, que estuda os efeitos fisiológicos das técnicas de meditação.</p>

<p class=Normal>"Descobrimos que quanto mais a prática for feita, mais profundas são as mudanças na expressão genômica", diz Benson. Ele e seus colegas estão agora investigando como os perfis de genes são alterados e se essas técnicas podem aliviar os sintomas em pessoas com pressão arterial alta, doença inflamatória intestinal e mieloma múltiplo, um tipo de câncer de medula óssea.</p>

