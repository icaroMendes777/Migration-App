<p class=Normal><b> <a name=T76></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>Um aviso
para o futuro<o:p></o:p></span></b></p> 

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Há esses fatos que devem ser contemplados por todos com freqüência, quer seja 
mulher ou homem, leigo ou ordenado. Quais?
 Eu sou o dono das minhas ações (kamma), herdeiro das minhas ações, nascido das minhas ações, 
relacionado através das minhas ações e tenho as minhas ações como árbitro. O que quer que eu faça, 
para o bem ou para o mal, disso me tornarei o herdeiro.” [AN.III.71] (<a href="../sutta/ANV.57.php">AN V.57</a>)</o:p></span></p>

