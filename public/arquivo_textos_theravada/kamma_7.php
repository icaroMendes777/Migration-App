<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O valor
prático dos ensinamentos de kamma pode ser resumido da seguinte forma:<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>Estabelece o entendimento de
     que as ações e os seus resultados estão sujeitos à causa e efeito, ao
     invés de encantos ditosos e objetos auspiciosos.<o:p></o:p></span></p>
<p class=Normal style='margin-left:.75in'>Demonstra que o êxito de
     qualquer aspiração depende das ações, encorajando a confiança em si mesmo
     e a diligência.<o:p></o:p></span></p>
<p class=Normal style='margin-left:.75in'>Desenvolve um senso de
     responsabilidade – em relação a si mesmo ao abandonar ações ruins e em
     relação aos outros ao agir com bondade.<o:p></o:p></span></p>
<p class=Normal style='margin-left:.75in'>Promove o entendimento de que todas
     as pessoas possuem o mesmo direito natural, de se deixarem degenerar ou
     de<span style="mso-spacerun: yes">  </span>melhorarem e se desenvolverem.<o:p></o:p></span></p>
<p class=Normal style='margin-left:.75in'>Mostra que as qualidades
     mentais, habilidades e comportamento são os elementos para se avaliar a
     ignobilidade ou nobreza das pessoas e que a discriminação com base na
     casta ou raça são desnecessárias e prejudiciais.<o:p></o:p></span></p>
<p class=Normal style='margin-left:.75in'>Mostra como aprender com o
     kamma passado (ações passadas), considerando as ações com base na razão,
     ao invés de simplesmente culpar os outros ou situações externas; através
     da análise das próprias ações, averiguando como corrigí-las e como
     melhorar a si próprio, determinando quais são as ações mais benéficas em
     uma dada situação.<o:p></o:p></span></p>
<p class=Normal style='margin-left:.75in'>Coloca o futuro, sob a forma de
     responsabilidade pessoal, nas mãos de cada indivíduo. <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Esses
valores podem ser considerados à luz das palavras do Buda apresentadas a
seguir:<o:p></o:p></span></p>
<p class=Normal>&nbsp;</p>
<p class=Normal><b> <a name=T71></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>O
significado geral<o:p></o:p></span></b></p> 

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>&quot;Intenção, eu lhes digo, é kamma. Pela intenção, a pessoa faz kamma
através do corpo, linguagem e mente.&quot;[AN.III.415] (<a
href="../sutta/ANVI.63.php">AN.VI.63</a>)<o:p></o:p></span></p>

