style='mso-ansi-language:PT-BR'>* * *<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“O que é benéfico passa desapercebido enquanto o tolo conta as estrelas.
O benéfico é o precursor do benéfico, qual a utilidade das estrelas?” [J.I.257]<o:p></o:p></span></p>

