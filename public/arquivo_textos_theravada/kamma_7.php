style='mso-ansi-language:PT-BR'>* * *<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>&quot;Estudante, os seres são os donos das suas ações, herdeiros das
suas ações, nascem das suas ações, estão atados às suas ações, possuem as suas
ações como refúgio. São as ações que distinguem os seres entre inferiores e
superiores.&quot;[MN.III.203] (<a href="../sutta/MN135.php">MN 135</a>)<o:p></o:p></span></p>

