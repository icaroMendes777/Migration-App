<p class=Normal><b> <a name=T74></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>Não adesão
a raça ou classe<o:p></o:p></span></b></p> 

<p class=Normal style='margin-left:.75in'>“Eu não digo que alguém seja um
brâmane devido à sua origem e linhagem. Se os impedimentos ainda nele se
ocultam, ele é apenas um dos que diz
‘Senhor.’ Mas aquele que está
desimpedido e não tem mais apego: ele é aquele que eu chamo de
brâmane. [<a href="../sutta/MN98.php#T27">MN98</a>]<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
“Pois o nome e o clã são atribuídos como meras designações mundanas; com origem nas convenções, estes são atribuídos aqui e ali.</p>


<p class=Normal style='margin-left:.75in'> "Para aqueles que desconhecem
este fato, idéias incorretas há muito tempo sustentam os seus corações; sem saber, eles nos declaram: ‘Uma pessoa é brâmane pelo nascimento.’</p>


<p class=Normal style='margin-left:.75in'> "Uma pessoa não é brâmane pelo nascimento, nem pelo nascimento ela não é brâmane. Através da ação ela é brâmane. Através da ação ela não é brâmane.</p>


<p class=Normal style='margin-left:.75in'>"Pois os homens são agricultores através das suas ações, e através das suas ações eles também são trabalhadores; e os homens são comerciantes através das suas ações, e através das suas ações eles também são criados.</p>


<p class=Normal style='margin-left:.75in'>"E os homens são ladrões através das suas ações, e através das suas ações eles também são soldados; e os homens são brâmanes através
das suas ações, e através das suas ações eles também são governantes.</p>


<p class=Normal style='margin-left:.75in'>“Portanto, é assim como aqueles verdadeiramente sábios vêm as ações como elas na verdade
são, enxergando a origem dependente, com habilidade nas ações e nos seus resultados.</p>


<p class=Normal style='margin-left:.75in'>"As ações é que
fazem o mundo girar, as ações fazem esta geração girar. Os seres vivos estão atados pelas
ações, da mesma forma que a roda da
carruagem pelo eixo. [<a href="../sutta/MN98.php#T55">MN98</a>]</p>

<p class=Normal align=center style='margin-left:.75in;text-align:center'><span
lang=PT-BR style='mso-ansi-language:PT-BR'>* * *<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Não é devido ao nascimento que alguém é um pária; não é devido ao nascimento 
que alguém é um brâmane. Através das ações alguém se torna um pária, através das ações alguém se torna um brâmane.” [Snp
142] (<a href="../sutta/SnpI.7.php">Snp I.7</a>)<o:p></o:p></span></p>

<p class=Normal align=center style='margin-left:.75in;text-align:center'><span
lang=PT-BR style='mso-ansi-language:PT-BR'>* * *<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Quando os membros das quatro castas – nobres guerreiros, brâmanes, comerciantes 
e trabalhadores - adotam a vida santa com a Doutrina e Disciplina declarada pelo Tathagata, 
eles abrem mão de seus nomes e clãs anteriores e são classificados simplesmente como 'os 
contemplativos, filhos do Sakya.'” (<a href="../sutta/UdV5.php">Ud V.5</a>)<o:p></o:p></span></p>

<p class=Normal align=center style='margin-left:.75in;text-align:center'><span
lang=PT-BR style='mso-ansi-language:PT-BR'>* * *<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“E, Vasettha, qualquer um, dentre as quatro castas, que como bhikkhu se 
torne um arahant com as impurezas destruídas, que viveu a vida santa, fez o que 
devia ser feito, depôs o fardo, alcançou o verdadeiro objetivo, destruiu os grilhões 
da existência e está completamente liberto através do conhecimento supremo, será proclamado 
como superior entre todos os demais de acordo com o Dhamma, não de outra forma.” [DN.III.97] (<a
href="../sutta/DN27.php#T31">DN 27.31</a>)<o:p></o:p></span></p>
