<p class=Normal><b> <a name=T72></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>Inteligência
acima da superstição<o:p></o:p></span></b></p> 

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Se fosse possível purificar o kamma ruim apenas com um banho num rio, então
os sapos, peixes, lontras, crocodilos e outros animais que vivem nos rios com
certeza teriam como destino o renascimento no paraíso...Se esses rios tivessem
a capacidade de eliminar o seu kamma ruim, então eles provavelmente também
eliminariam o seu kamma bom.” [Thag. 240-244]<o:p></o:p></span></p>

