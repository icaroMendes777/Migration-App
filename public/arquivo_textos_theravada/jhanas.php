<p class=Normal>Observamos que alguns dos grupos que fazem parte do conjunto
das “Asas do Despertar”, (<i>bodhi-pakkhiya-dhamma</i>), indicam que jhana é
uma condição para a sabedoria,<span style="mso-spacerun: yes">  </span>enquanto
que outros indicam que a sabedoria é uma condição para jhana. Se colocarmos
essas duas asserções dentro do contexto da condicionalidade isto/aquilo, elas
transmitirão a idéia de que jhana e sabedoria na prática se suportam
mutuamente. O Dhammapada menciona este ponto de modo explícito:</p>

<p class=Normal style='margin-left:.75in'>Não existe concentração, (<i>jhana</i>),
sem sabedoria, (<i>pañña</i>), e não existe sabedoria sem concentração. Aquele
que tem ambos, concentração e sabedoria, está mais próximo de Nibbana.<span
style="mso-spacerun: yes">  </span>(Dhp 372)</p>

<p class=Normal>Já o <i><a href="../sutta/ANIV.94.php">Samadhi Sutta<span
style='font-style:normal'> </span>(AN IV.94)</a></i> e o <i><a
href="../sutta/ANIV.170.php">Yuganaddha Sutta<span style='font-style:normal'> </span>(AN
IV.170)</a></i> mostram que a diferença entre os dois padrões causais está
relacionada com diferenças entre os meditadores: alguns desenvolvem fortes
poderes de concentração antes de desenvolver a sabedoria, enquanto que outros obtêm
uma sólida compreensão teórica do Dhamma antes de desenvolver uma forte
concentração. Em ambos os casos, tanto a forte concentração como a sólida
sabedoria são necessárias para concretizar o Despertar. O <i><a
href="../sutta/MN149.php">Mahasalayatanika Sutta (MN 149)</a></i> assevera que
quando a prática atinge o cume do seu desenvolvimento, a concentração e a
sabedoria agem em combinação. Os parágrafos que seguem tratam deste tópico com
mais detalhes. </p>


<p class=Normal>O papel de jhana como condição para a sabedoria transcendente
é um dos temas mais controversos na tradição Theravada. Três posições básicas
têm sido advogadas nos textos modernos. Uma, seguindo a tradição dos
comentários, afirma que jhana não é necessário para qualquer um dos quatro
níveis do Despertar e que há uma classe de indivíduos – denominados meditadores
de “dry insight”, (insight seco) – que são “libertados através da sabedoria”
apoiados num nível de concentração inferior ao jhana. Uma segunda posição,
mencionando um texto do Cânone, (AN III.88), afirma que a maestria na
concentração é obtida apenas no nível de não retorno, e defende que jhana é
necessário para alcançar os níveis de não retorno e Arahant, mas não para os
primeiros níveis do Despertar. A terceira posição afirma que alcançar a realização,
pelo menos, do primeiro nível de jhana é essencial para todos os quatro níveis
do Despertar. </p>

<p class=Normal>A evidência do Cânone suporta a terceira posição, mas não as
outras duas. Tal como indicado no <i><a href="../sutta/MN117.php">Mahacattarisaka
Sutta (MN 117)</a></i>, a realização do <i>sotapanna</i>, (aquele que entrou na
correnteza), possui oito fatores, um dos quais é a concentração correta,
definida como jhana. Na verdade, de acordo com esse discurso, jhana se encontra
no núcleo do caminho do <i>sotapanna. </i>Em segundo lugar, não há nenhum
trecho no Cânone que descreva o desenvolvimento da sabedoria transcendente sem
pelo menos alguma habilidade relacionada a jhana. A afirmação de que a maestria
na concentração é obtida apenas no nível de não retorno deve ser interpretada à
luz da distinção entre maestria e realização. Um <i>sotapanna</i> pode haver
alcançado jhana sem obter a completa maestria; a sabedoria desenvolvida no
processo da obtenção completa da maestria, na prática de jhana, irá então conduzir
o meditador ao nível de não retorno. Quanto ao termo “libertados através da
sabedoria,” o <i><a href="../sutta/MN70.php">Kitagiri Sutta (MN 70)</a></i>
mostra que isto se refere àqueles que se tornaram Arahants sem experimentar as
realizações imateriais, (jhanas imateriais). Não denota alguém que não tenha
experimentado jhana.</p>

<p class=Normal>Parte da controvérsia, com relação a essa questão, pode ser
explicada pelo fato de que os comentários definem jhana de uma forma que contém
pouca semelhança com a descrição do Cânone. O <i>Visudhimagga,</i> (O Caminho
para a Purificação) - que é a pedra angular dos comentários – toma como
paradigma, para a prática da meditação, um método denominado kasina, no qual o
meditador fixa os olhos num objeto externo até que a imagem do objeto esteja
estampada na mente dele. Essa imagem, então, dá origem a um contra-sinal que é
dito ser o indicador da realização da concentração de acesso, um prelúdio
necessário ao jhana. O texto, então, tenta encaixar todos os demais métodos de
meditação dentro do molde da prática com a kasina, de maneira que eles também
dêem origem a contra-sinais, mas o próprio texto admite que a meditação com a
respiração não se encaixa bem nesse molde: com os demais métodos, quanto mais
forte for o foco da pessoa, mais vívido se tornará o objeto e mais próximo ele
estará de produzir o contra-sinal; mas com a respiração, quanto mais forte for
o foco da pessoa, mais difícil se tornará a detecção do objeto. Como resultado,
o texto diz que apenas os Budas e os discípulos dos Budas vêm na respiração um
objeto apropriado para alcançar jhana. </p>

<p class=Normal>Nenhuma dessas afirmações encontra respaldo no Cânone.
Embora a prática chamada kasina seja mencionada de modo superficial em alguns
dos discursos, o único texto no qual ela é descrita em algum nível de detalhe,
no <i><a href="../sutta/MN121.php">Culasuññata Sutta – MN121</a></i>, não faz
menção à fixação dos olhos num objeto e à obtenção de um contra-sinal. Se a
meditação com base na respiração fosse apropriada apenas para os Budas e os
seus discípulos, não haveria razão para o Buda tê-la ensinado com tanta
freqüência e para uma gama tão variada de pessoas. Se a obtenção de um
contra-sinal fosse essencial para alcançar jhana, seria de se esperar que isso
fosse incluído nos passos da meditação baseada na respiração por meio dos
símiles empregados para descrever jhana, mas esse não é o caso. Alguns adeptos
do Theravada insistem em que questionar os comentários é um sinal de
desrespeito para com a tradição, mas parece ser um sinal de maior desrespeito para
com o Buda – ou com os escribas do Cânone – ao assumir que ele, ou eles,
deixaram de lado algo absolutamente essencial para a prática. </p>

<p class=Normal>Todos esses pontos parecem indicar que o significado de jhana
nos comentários é algo completamente distinto daquilo que é mencionado no
Cânone. Devido a essa diferença, podemos afirmar que os comentários estão
certos em considerar o tipo de jhana que eles propõem como desnecessário para o
Despertar, mas o Despertar não pode ser concretizado sem a realização de jhana
no sentido mencionado no Cânone. </p>

<p class=Normal>No capítulo anterior, fizemos um esboço de como jhana, no
sentido referido pelo Cânone, pode agir como base para a sabedoria
transcendente. Para recapitular: ao alcançar qualquer um dos primeiros sete
níveis de jhana, o meditador se afasta ligeiramente do objeto de jhana –
entrando no quinto fator da nobre concentração correta, (<i><a
href="../sutta/ANV.28.php">Samadhanga Sutta - AN V.28</a></i>), para perceber
como a mente se relaciona com o objeto. Ao fazer isso, o meditador observa o
processo de causação enquanto este desempenha o seu papel de<span
style="mso-spacerun: yes">  </span>trazedor, trazendo a mente para o jhana,
junto com os vários elementos da fabricação mental necessários para mantê-la
ali, (<i><a href="../sutta/MN140.php">Dhatu-vibhanga Sutta – MN 140</a></i>). O
<i><a href="../sutta/MN111.php">Anupada Sutta – MN 111</a></i> relaciona<span
style="mso-spacerun: yes">  </span>esses elementos num razoável nível de
detalhe. O fato de que esse discurso enfatiza as incríveis habilidades de Sariputta,
o principal discípulo do Buda em relação à sabedoria, significa que não é
necessário que todos os meditadores percebam todos esses elementos em tal
detalhe. O que é essencial é que o meditador desenvolva um senso de desapego em
relação ao estado de jhana, vendo que mesmo a sensação relativamente estável de
refinado prazer e equanimidade proporcionada é artificial e fruto da volição,
inconstante e insatisfatória, (<i><a href="../sutta/MN140.php">Dhatu-vibhanga
Sutta – MN 140</a></i>), um estado fabricado de muitos eventos distintos e
portanto, não merecendo que com ele nos identifiquemos. Jhana desse modo se
torna um caso teste ideal para a compreensão das operações de kamma e origem
dependente na mente. A sua estabilidade proporciona ao discernimento uma base
firme para ver com clareza; a refinada sensação de prazer e equanimidade
permitem que a mente compreenda que mesmo os estados mundanos mais refinados
envolvem a inconstância e o estresse comum a todos os fenômenos frutos da
volição. O <i><a href="../sutta/ANIII.128.php">Anuruddha Sutta (AN III.128)</a></i><span
style="mso-spacerun: yes">  </span>lista algumas ações verbais e mentais, que
acompanham o exercício de poderes supra-humanos, que podem ser vistas de forma
semelhante, como tópicos a serem analisados para dar origem a um senso de desapego.
O desapego que resulta em ambos os casos permite ao meditador experimentar o
desaparecimento e a cessação das últimas atividades restantes na mente, mesmo a
própria atividade do discernimento em si. Quando esse processo amadurece
plenamente, ele conduz à completa renúncia, resultando na clara compreensão e
libertação do Arahant.</p>

<p class=Normal>Em contraste com o tema do papel de jhana como condição para
a sabedoria, o papel da sabedoria como condição para jhana não apresenta
controvérsias. A sabedoria auxilia jhana em dois níveis: mundano e
transcendente. No nível mundano, ela possibilita ao meditador a percepção dos
vários fatores que compõem um determinado estado de jhana para que possa
controlá-los e descartar os fatores que impedem o alcance de um nível de jhana
mais elevado. Isto novamente envolve a reflexão que constitui o quinto fator da
nobre concentração correta, mas neste caso os resultados se mantêm no nível
mundano. Por exemplo, ao obter a maestria do primeiro nível de jhana e
refletindo sobre os elementos de estresse nele contidos, o meditador pode
perceber que o pensamento aplicado e o pensamento sustentado devem ser
abandonados porque se tornaram desnecessários para manter a concentração,
exatamente como os moldes empregados para despejar o concreto se tornam
desnecessários depois que o correto endureceu. Ao abandonar esses fatores, o
meditador então passa para o segundo nível de jhana. O <i><a
href="../sutta/ANIX.34.php">Nibbana Sutta (AN IX.34)</a></i> descreve os
fatores que, em seqüência, são abandonados à medida que se vai obtendo níveis
cada vez mais elevados de concentração.</p>

<p class=Normal>Num nível transcendente, a sabedoria que precipita o
Despertar resulta num nível de jhana supramundano chamado fruto do
conhecimento, que é descrito no <i><a href="../sutta/ANIX.37.php">Ananda Sutta
(AN IX 37)</a></i> – um tipo de jhana independente de todas as percepções,
(rótulos mentais), e processos baseados na volição, além de todas as limitações
do cosmo, do tempo e do presente: o antegozo do Arahant, vivenciado nesta vida,
o completo e absoluto Desatamento experimentado pela mente desperta após a
morte. </p>


