<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Um dos primeiros obstáculos
que as pessoas no Ocidente se deparam com freqüência quando estão aprendendo
acerca do Budismo é o ensinamento de anatta, com freqüência traduzido como
'não-eu'. Esse ensinamento é um obstáculo por duas razões. Primeiro, a idéia de
que não existe um eu não se encaixa bem com outros ensinamentos Budistas, tais
como a doutrina de kamma e renascimento. Se não existe um eu, quem experiencia
os resultados do kamma e renasce? Segundo, ela não se encaixa bem com a nossa
referência Judaico-Cristã que possui como pressuposto básico a existência de
uma alma eterna ou eu. Se não existe um eu, qual o propósito de uma busca
espiritual? Muitos livros tentam responder essas questões, mas se você procurar
no Cânone em Pali - o registro mais antigo que existe dos ensinamentos do Buda
- você não irá encontrar absolutamente nenhuma referência. Na verdade, em uma
ocasião em que o Buda foi questionado diretamente se existe ou não um eu, ele
se recusou a responder. Quando mais tarde perguntado porquê, ele disse que
acreditar que existe um eu ou de que não existe um eu, significa cair em formas
extremas de entendimento incorreto que impossibilitam a prática do caminho
Budista. Por isso a questão deve ser deixada de lado. Para entender o que o seu
silêncio sobre essa questão informa acerca do significado de anatta, precisamos
primeiro entender os seus ensinamentos acerca de como as questões devem ser
perguntadas e respondidas e como interpretar as suas respostas. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O Buda dividiu todas as
questões em quatro classes: aquelas que merecem uma resposta direta ( sim ou
não); aquelas que merecem uma resposta analítica, definindo e qualificando os
termos da questão; aquelas que merecem uma contra-pergunta, devolvendo a bola a
quem levantou a questão; e aquelas que devem ser colocadas de lado. A última
classe de questões corresponde àquelas que não conduzem ao fim do sofrimento e
estresse. A primeira tarefa de um mestre, quando questionado, é identificar a
que classe pertence a pergunta e então responder da maneira adequada. Você por
exemplo, não responde sim ou não a uma questão que deve ser posta de lado. Se
você é a pessoa que está perguntando e obtém uma resposta, você deve determinar
até que ponto a resposta deve ser interpretada. O Buda disse que existem dois
tipos de pessoas que distorcem os seus ensinamentos: aquelas que fazem deduções
acerca de enunciados dos quais não se deve fazer nenhuma dedução e aquelas que
não fazem deduções quando deveriam fazê-las. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Essas são as regras básicas
para interpretar os ensinamentos do Buda mas se olharmos a forma como a maioria
dos autores trata a doutrina de anatta, veremos que essas regras básicas são
ignoradas. Alguns autores argumentam em defesa da interpretação de não-eu
dizendo que o Buda negou a existência de um eu eterno ou independente mas isso
equivale a dar uma resposta analítica a uma questão que o Buda mostrou que deve
ser posta de lado. Outros tentam fazer deduções de alguns enunciados contidos
nos discursos que parecem indicar que não existe um eu, mas com segurança
podemos dizer que se alguém força esses enunciados a dar uma interpretação a
uma questão que deveria ser colocada de lado, estará fazendo deduções onde elas
não devem ser feitas. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim, ao invés de
responder &quot;não&quot; à questão se existe ou não existe um eu -
inter-conectado ou separado, eterno ou não - o Buda percebeu que a questão
estava mal formulada desde o princípio. Porque? Não importa como se defina a
fronteira entre &quot;eu&quot; e &quot;outro&quot;, a noção do eu implica um elemento
de auto identificação e apego e, por isso, sofrimento e estresse. Isso vale
tanto para um eu inter-conectado que não reconhece um &quot;outro&quot; como
para um eu separado. Se alguém se identifica com toda a natureza, sofre com
cada árvore que cai. Isso também vale para um universo que seja completamente
&quot;outro&quot;, em que a sensação de alienação e futilidade se tornaria tão
debilitante fazendo com que a busca pela felicidade - a própria ou de outros -
se tornasse impossível. Por essas razões, o Buda aconselhou que não se desse
atenção a questões como &quot;Eu existo?&quot; ou &quot;Eu não existo?&quot;
pois não importa como elas sejam respondidas, conduzirão ao sofrimento e ao
estresse. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Para evitar o sofrimento
implícito nas questões de &quot;eu&quot; e &quot;outro&quot;, o Buda ofereceu
uma maneira alternativa de como encarar o que experimentamos: as Quatro Nobres
Verdades do sofrimento, a sua causa, a sua cessação e o caminho que leva à
cessação. Ao invés de ver essas verdades como pertencendo a um eu ou outro, ele
disse que se deveria reconhecê-las simplesmente pelo que elas são, em si mesmas
e por si mesmas, da forma como são experimentadas diretamente, e então realizar
a tarefa apropriada a cada uma delas. O sofrimento deve ser compreendido, a sua
causa abandonada, a sua cessação realizada e o caminho para a cessação
desenvolvido. Essas tarefas constituem o contexto no qual a doutrina de anatta
pode ser melhor compreendida. Se você desenvolve o caminho da virtude,
concentração e sabedoria até o ponto em que alcança um estado de bem estar
tranqüilo e utiliza esse estado de tranqüilidade para olhar para a sua
experiência sob a perspectiva das Quatro Nobres Verdades, as questões que
ocorrem na mente não são &quot;Existe um eu? O que é o meu eu?&quot; mas ao
invés disso &quot;Estou sofrendo porque estou me apegando a este fenômeno em
particular? E isso é realmente parte de mim, meu? Se gera sofrimento mas não é
realmente parte de mim, ou meu, porque me apegar?&quot; Essas últimas questões
merecem respostas diretas, pois elas auxiliam a compreender o sofrimento e
gradualmente eliminar o apego - o sentimento residual de auto identificação -
que é a sua causa, até que finalmente todos os traços de auto identificação
desapareçam e tudo que reste seja a liberdade sem limites.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Nes<a name=PVW>se se</a>ntido,
o ensinamento acerca de anatta não se trata de uma doutrina de não-eu, mas de
uma estratégia de despersonalização para se desfazer do sofrimento através do
abandono da sua causa, conduzindo à felicidade máxima e imortal. Nesse ponto,
questões acerca do eu ou não-eu são colocadas de lado. Uma vez que essa liberdade
completa é experimentada, porque haveria qualquer preocupação com quem a está
experimentando ou se se trata ou não de um eu? <o:p></o:p></span></p>


