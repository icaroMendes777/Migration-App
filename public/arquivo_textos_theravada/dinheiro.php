<p class=Normal>Neste artigo, vou tratar da questão controversa das relações de um monge ou monja com o dinheiro.</p>

<p class=Normal>    A questão tem sido controversa por mais de 2.000 anos. Cerca de 200 anos depois da morte do Buda, houve "disputas intermináveis sendo que em nenhum dos discursos os argumentos estavam claros" [<a href="#N1">1</a>]. <a name=R1></a>Esta disputa surgiu porque uma grande comunidade de monges estavam aceitando dinheiro em desafio ao Vinaya. O protocolo dessa disputa ficou conhecido como o Segundo Concílio e plantou a semente do primeiro grande cisma no mundo Budista, que aconteceu logo depois.</p>

<p class=Normal>    Naquela ocasião, como agora, não há nenhuma justificativa para alguma incerteza em relação a esse ponto, pois as próprias palavras do Buda deixam claro, como veremos a seguir.</p>

<p class=Normal><b>    Monges e Dinheiro</b></p>

<p class=Normal>Os monges Budistas (bhikkhus) e as monjas (bhikkhunis) não estão autorizados a aceitar dinheiro em seu próprio benefício. Nem lhes é permitido dizer a um leigo em quem depositem confiança para que receba dinheiro em seu nome e o guarde (como por exemplo numa conta bancária pessoal). Tais práticas são expressamente proibidas na regra 18 da seção do Vinaya chamada <i>Nissaggiya Pacittiya</i>.</p>

<p class=Normal>Nem monges ou monjas podem comprar e vender coisas em seu próprio benefício usando dinheiro. Isso é proibido pela regra 19 no <i>Nissaggiya Pacittiya</i>.</p>

<p class=Normal><a name=R2></a>    Algumas pessoas argumentam que estas duas regras se referem apenas ao ouro e à prata, mas esse ponto de vista é indefensável. O Vinaya afirma especificamente que estas regras abrangem "qualquer meio usado no mundo dos negócios", [<a href="#N2">2</a>] ou seja, qualquer meio de troca.</p>

<p class=Normal>    Outras pessoas tentam contornar esta regra dizendo que é apenas uma regra menor, inaplicável à vida monástica na atualidade. De fato, o Buda certa vez disse que a Sangha poderia abolir as regras "menores e sem importância". [<a href="#N3">3</a>]</p>

<p class=Normal>    <a name=R3></a>Mas isso seria uma regra menor? ...</p>

<p class=Normal>        "Monges, há essas quatro máculas devido às quais o sol e a lua não brilham, não radiam, não resplandecem. Quais são as quatro? Nuvens de chuva ... nuvens de neve ... fumaça e poeira ... e um eclipse. Também, monges, há essas quatro máculas devido às quais os monges não brilham, não radiam, não resplandecem. Quais são as quatro? Beber álcool ... entregar-se à relação sexual ... aceitar ouro ou dinheiro ... obter os requisitos através de um modo de vida incorreto. Estas são as quatro máculas devido às quais o sol e a lua não brilham, não radiam, não resplandecem." [<a href="#N4">4</a>]</p>

<p class=Normal><a name=R4></a>    Obviamente, o Buda pensava que a regra que proíbe a aceitação de ouro ou dinheiro era, de fato, uma regra muito importante.</p>

 <p class=Normal>   A não aceitação de dinheiro sempre foi uma das observâncias fundamentais daqueles que deixaram o mundo. O dinheiro é a medida da riqueza e para a maioria das pessoas a riqueza material é o objetivo da vida. Através da renúncia ao dinheiro os monges e monjas enfaticamente demonstram sua rejeição completa às atividades mundanas. Em uma tacada eles se distanciam de modo significativo do restante da grande maioria das pessoas e, assim, tornam-se um lembrete constante de que uma vida baseada no esforço pelo acúmulo de dinheiro não é a única maneira de viver. Através da renúncia ao dinheiro eles abdicam em grande parte do seu poder de manipular o mundo e de satisfazer os seus desejos. Assim, como o Buda disse certa vez, quando perguntado se aos monges e monjas era permitido manusear dinheiro:</p>

<p class=Normal>        "Quem aceita ouro ou dinheiro também aceita os cinco elementos do prazer sensual, e quem aceita os cinco elementos do prazer sensual, alguém pode ter a certeza de que esse não é o caminho de um contemplativo, esse não é o caminho de um bhikkhu." [<a href="#N5">5</a>]

