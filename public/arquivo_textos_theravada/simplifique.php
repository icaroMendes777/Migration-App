<p class=Normal>Considerando o Dhamma, olhando de um ângulo, é muito complicado, bastante complexo, há muito por saber, muito para entender, muita informação a ser digerida. Visto de um outro ponto de vista é bastante direto – só uma questão de segui-lo, de praticá-lo. Há um certo elemento, particularmente notável no temperamento Ocidental, que nos faz acreditar que quanto mais informação temos, mais saberemos sobre alguma coisa, e por conseguinte, quanto mais informação tivermos melhor seremos na prática do Dhamma. Isto na realidade não é verdade. </p>

 

<p class=Normal>Muita importância deve ser dada à paciência, ser capaz de ser paciente com as próprias experiências, observando a si mesmo, observando o mundo em volta de si e aprendendo a confiar no observador, aquele que observa atentamente, a habilidade da mente humana de prestar atenção em si mesma. Quando falamos de libertação ou iluminação, na verdade trata-se apenas de prestar atenção, para o que a atenção está direcionada. Logo, é um aprendizado observar a si mesmo, as próprias experiências, reconhecer a qualidade da mente. </p>

 

<p class=Normal>O Buda deu grande ênfase à qualidade do sofrimento, da insatisfação. As Quatro Nobres Verdades tomam por base a observação dessa qualidade da insatisfação. É algo a ser conhecido. É um dever para consigo mesmo entender a insatisfação. Daí, você pode partir para alguma outra coisa. O problema é como nos relacionamos com o mundo à nossa volta. Pela forma com que nos relacionamos uns com os outros, tendemos a criar ou experimentar insatisfação. Então, nos prendemos a isso, nos apegamos a isso, julgamos isso, tentamos evitar isso, criamos cenários incríveis ao redor disso, procuramos alguém para culpar por isso, ou sentimos pena de nós mesmos. Assim, criamos toda uma gama de reações em torno da insatisfação. Mas o que o Buda disse é que tudo o que se tem a fazer é apenas conhecer, você só tem que conhecer a insatisfação. </p>

 

<p class=Normal>A qualidade do conhecer deve ser direcionada, focalizada na nossa experiência e aí aprendemos a reconhecer que este conhecer é um ponto de equilíbrio, não é querer, não é afirmar ou rejeitar, não é desejar ou não desejar, é o equilíbrio das faculdades da mente. Experienciamos o mundo com o corpo e a mente, e estas são as ferramentas que temos. Damos voltas entre as faculdades sensoriais do corpo e as faculdades da mente, a habilidade de criar e experimentar tons emocionais de felicidade ou sofrimento, a habilidade de se lembrar e conceituar, dar rótulos às coisas, a percepção, a habilidade de agir de forma volitiva, iniciar processos de pensamento e estar consciente do mundo ao nosso redor. Estas são as ferramentas que temos. </p>

 

<p class=Normal>A prática do Dhamma é aprender a qualidade do conhecer, conhecer o mundo à nossa volta, tanto o mundo material do corpo físico como as seis esferas dos sentidos da mente, as faculdades da mente. Conhecer apenas, não reagir às proliferações que criamos em torno disso, e permanecer com o conhecer tão somente. Assim, esta prática é portanto a de conhecer. Parece difícil porque as coisas mais simples são as mais difíceis de se manter. </p>

 

<p class=Normal>Por conseguinte precisamos desenvolver certas ferramentas, certas qualidades. O Dhamma nos oferece um arcabouço teórico que pode parecer complicado mas facilita esse conhecer. Ele requer que voltemos ao coração humano que é capaz de conhecer, capaz da paz, capaz de criar o inferno ao redor de nós mesmos e também de criar  mundos celestiais. Temos de ver este ponto de claridade e quietude dentro de nós mesmos de forma a pararmos de criar mundos ao nosso redor. Uma vez Ajaan Chah e um grupo de seus discípulos foi visitar um discípulo famoso de Ajaan Mun, Luan Por Khau. Ajaan Sumedho estava neste grupo. Eles ouviram um ensinamento do Dhamma e quando estavam saindo, Ajaan Sumedho, sendo o mais novo do grupo teve de ser o último a sair da sala. No momento em que estava saindo Luan Por Khau levantou-se rapidamente e veio até ele apontando para o seu coração e disse, “está tudo aqui, tudo aqui”. Toda a palestra, as explicações, tudo volta ao coração, temos de ver isto com clareza e prestar atenção à mente, ao coração. </p>

 

<p class=Normal>Esta é a razão do Buda ter nos ensinado as Quatro Nobres Verdades tal como estas estão no coração, na mente que nos motiva. Todos os seres sencientes preferem a felicidade ao sofrimento. Então somos motivados a tentar a libertação do sofrimento. Com freqüência, no entanto, nossas tentativas são superficiais e mal orientadas, sendo apenas alívios temporários para o sofrimento. Adiamos lidar com o sofrimento para um futuro distante, ou um futuro imediato. O Buda disse que a forma que se deve lidar é entendendo as causas pois somente podemos compreender algo quando entendemos a sua causa. O Buda apontou que com freqüência o nosso desentendimento da verdade, ou realidade, ocorre por <i>avijja</i>, ou falta de conhecimento. </p>

 

<p class=Normal>O termo <i>avijja</i> é frequentemente traduzido como ignorância mas é na realidade falta de verdadeiro conhecimento. Através desta falta de verdadeiro conhecimento, diferentes tipos de desejo são criados: o desejo de buscar a gratificação sensual, o desejo por afirmar o eu, o desejo por vir-a-ser, o desejo pela auto-negação, pela aniquilação, pela rejeição da experiência; não querer experimentar coisas também é um desejo. Então este agarrar e repelir, esse apego pela experiência é a causa real de nosso sofrimento. Portanto é o abandono do desejo que realiza a cessação do sofrimento. </p>

 

<p class=Normal>Nós não abandonamos o sofrimento mas tentamos afastar a causa imediata deste e tentamos buscar algo mais satisfatório. Isto não é lidar com as causas reais, que têm de ser vistas como na verdade são e abandonadas. Abrir mão é algo que tem que ser sentido, sentido de forma consciente. Abrir mão do apego nos nossos corações às reações emocionais às experiências, aos relacionamentos e aos julgamentos, de como as coisas deveriam ou não deveriam ser. Esse abandono de todas estas coisas, abrir mão de tudo isso, é resumido em um breve ensinamento dado pelo Buda  - “Todos os <i>dhammas</i> não devem ser objetos de apego”. Esse é o cerne de seu ensinamento – tudo deve ser abandonado, soltado. </p>

 

<p class=Normal>A natureza do desejo é acumular, agarrar, apegar-se às coisas, prender-se. Temos de estabelecer na nossa prática a atenção para essa tendência e tentar ir contra essa inclinação, abrindo mão de tudo. Quando você realmente enxergar o sofrimento você irá querer deixá-lo ir. Quanto mais claramente você vê o sofrimento, mais você intencionará deixá-lo. De certa forma é similar ao método de capturar macacos. Um pequeno buraco é feito em um coco; deve ser apenas grande o suficiente para que o macaco possa passar a mão. Um pedaço de fruta é colocada dentro do coco. Então quando um macaco vem, bastante curioso, ele coloca a sua mão dentro do coco e ao encontrar a fruta a agarra. Mas ele fica preso visto que o buraco é muito pequeno para a mão fechada, mas ele não solta da fruta. O desejo pela gratificação é mais forte do que o reconhecimento do sofrimento que se seguirá quando o caçador o pegar pela nuca. Se o macaco fosse capaz de realmente ver o sofrimento, seria fácil abrir mão da fruta e fugir. </p>

 

<p class=Normal>Nós fazemos a mesma coisa. O sofrimento está presente todo o tempo mas não o abandonamos porque não o vemos com clareza suficiente. Assim que nós o vemos, deveríamos abandoná-lo. Mas não reconhecemos o sofrimento e o carregamos por aí conosco, a aversão, a má-vontade, a raiva, e por períodos longos de tempo – minutos, horas, dias, semanas, meses, anos – pois podemos justificá-lo de alguma forma. Somos também capazes de sofrer tremendamente por coisas que percebemos poderiam nos dar prazer, e que em certo nível podem até ser prazerosas, mas o sofrimento lhes é inerente. O claro reconhecimento do sofrimento é desta forma relacionado à habilidade de abrirmos mão. E a habilidade de abrir mão é claramente relacionada ao grau de atenção e plena consciência, a estabilidade do conhecer. Então voltamos novamente para esta qualidade de conhecer, do estabelecimento da atenção plena e da plena consciência. </p>

 

<p class=Normal>Este é o propósito deste caminho, todo o propósito de nossa prática é facilitar esta qualidade de clareza. Não se trata de plena atenção e plena consciência passivas, há um sentido ou responsabilidade moral nisso, um sentido de paciência e determinação, a habilidade de fazer o esforço. Nosso caminho trata de desenvolver a virtude, ou <i>sila</i>, ver claramente e tomar a responsabilidade em um sentido moral pelas nossas ações e pela fala. E precisamos desenvolver na nossa prática a qualidade da renúncia e a qualidade da sabedoria – questionar, investigar e refletir. </p>

 

<p class=Normal>Nosso treinamento, no nível do treinamento mental, é um treinamento na realização  do esforço para reconhecer os meios para cultivar aquilo que é benéfico e deixar de lado aquilo que é prejudicial. É o desenvolvimento da estabilidade da mente, a concentração, firmeza da mente. Essa firmeza que tem que ser desenvolvida é uma estabilidade emocional, no sentido do coração e mente e não no sentido da mente analítica. É a habilidade de não ser levado por nossas preferências habituais, o querer e o não-querer, e de estabelecer um equilíbrio. Concentração às vezes tem o sentido de se ter um foco, de exclusão. A exclusão, obscurecimento da mente, não nos leva a uma mente realmente estável e tranqüila. Deve haver uma abertura, não reagindo aos gostos e desgostos, uma habilidade de observar, permanecer apenas com o conhecer. Portanto a firmeza se relaciona à habilidade de reconhecimento, à habilidade de observar, sem um sentido de foco num modo excludente. </p>

 

<p class=Normal>Logo, precisamos desenvolver as qualidades da investigação. E o Buda deu os parâmetros, as fronteiras da investigação, o que investigar e as ferramentas para se investigar a experiência. A estrutura das Quatro Nobres Verdades, os cinco <i>khandas</i> ou agregados do ser, as seis esferas dos sentidos, são ferramentas para delinear a nossa experiência. Nos permitem reconhecer os padrões da nossa mente, os padrões da nossa experiência. </p>

 

<p class=Normal>Portanto, quando sentamos em meditação é muito importante ter uma estrutura, um modelo que nos guie na investigação da nossa experiência. Se apenas sentarmos e observarmos nossa inspiração e expiração, sem demora a mente começa a vaguear e se prende em alguma coisa ou outra, ou então se chateia e colapsa em si mesma e estaremos sentados num estado de torpor. Quando a mente está em <i>samadhi</i> está pronta para a tarefa, a tarefa de um meditador, que é de investigar a própria experiência, o que o motiva, o que causa a proliferação na mente, o que é que cria sofrimento, o que cria um ponto de equilibrio na mente. </p>

 

<p class=Normal>Estas são questões que precisam ser investigadas quando estamos engajados na meditação. Algumas vezes sentamos e esperamos por uma iluminação que baixe sobre nós e nos liberte de toda a confusão – mas não é assim que a mente funciona. A mente deve ser empregada para ver e investigar os problemas que estão presentes nas experiências, de forma a entender a mente e a si mesmo mais claramente, assim é que alguém desenvolve a prática para encorajar a atenção plena, retornando à respiração. A inspiração e a expiração, usadas para aclarar os movimentos da mente. Usando como referência a respiração entrando e saindo para ver para onde a mente se move, iluminar os movimentos da mente. Esta observação cuidadosa dentro dos parâmetros do objeto de meditação aclara, faz com que o meditador entenda a natureza da mente. </p>

 

<p class=Normal>A quietude mental não é obtida bloqueando as coisas ou forçando a mente para um ponto de imobilidade. Quando mais você força a mente, mais tensa esta se torna. O que é necessário é a aplicação da mente utilizando-se as ferramentas que nos foram definidas pelo Buda. Com a aplicação do esforço a prática se amplia e se percebe que lacunas no entendimento são preenchidas e as dúvidas e desentendimentos superados. Assim este aspecto da sabedoria não é apenas um conhecimento passivo ou um tanto de informação obtida de um livro ou de um professor. A sabedoria é obtida através da aplicação da própria mente, investigando a própria mente com honestidade. Com freqüência as nossas mentes criam distrações para si mesmas, criando histórias ao redor de nós mesmos. A não ser que você veja a mente como ela é, você continuará aceitando estas histórias, toda esta proliferação. Então temos que desenvolver um claro entendimento de modo que possamos abrir mão, abandonar as criações e proliferações da mente, deixar que tudo isto cesse. </p>

 

<p class=Normal>Conforme nos tornamos mais e mais familiarizados com o conhecer, somos capazes de encontrar uma qualidade de desprendimento, um ponto de quietude como parte desse conhecer. Há um belo símile que Ajaan Chah utilizava para descrever as proliferações da mente. Ele as comparava com as rodas de um carro-de-boi pois estas criam sulcos profundos que parecem não ter fim. As rodas não são tão grandes, mas os sulcos são muito longos. O propósito de nosso treinamento e nossa prática é parar esse carro-de-boi, permitir que ele descanse. E esse é o ponto para o qual nossa prática deveria estar seguindo, na direção de um ponto de descanso. </p>

 

