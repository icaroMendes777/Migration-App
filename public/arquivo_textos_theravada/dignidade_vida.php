<p class=Normal>Perguntar o que significa viver com
dignidade pode parecer estranho em uma época como a nossa em que a luta
frenética para assegurar nosso sustento praticamente não deixa nenhum tempo
disponível para ponderar questões de tamanha magnitude. </span></p>

<p class=Normal>Porém se pararmos por um momento para
pensar um pouco acerca dessa questão, nos daremos conta rapidamente de que não
se trata de uma reflexão a toa de alguém que tem muito tempo disponível para
pensar. A questão não somente toca no verdadeiro sentido das nossas vidas mas
também vai além da nossa busca por um sentido, penetrando na essência da
cultura contemporânea. Pois se não é possível viver com dignidade então a vida
não possui um significado transcendente e nesse caso nosso único objetivo no
curto período de tempo que temos, deveria ser de agarrar o maior número de
momentos excitantes que pudermos antes que as luzes se apaguem de uma vez.
Porém se vemos algum sentido na idéia de viver com dignidade então devemos
considerar se atualmente estamos organizando as nossas vidas da maneira como
deveríamos e, de maneira mais abrangente, se a nossa cultura encoraja um modo
de vida digno. Apesar da idéia de dignidade parecer simples à primeira vista,
ela é na verdade bastante complexa. </span></p>


<p class=Normal>O dicionário Webster de 1936 define
dignidade como &quot;elevação de caráter, valor intrínseco,
excelência,...nobreza nos modos, aspecto ou estilo&quot;; já o Thesaurus de
1977 coloca dignidade juntamente com &quot;prestígio, estima, reputação, honra,
glória, bom nome, fama&quot; - o que evidencia que ao longo dos últimos
quarenta anos o significado central dessa palavra sofreu alguma modificação.
Quando perguntamos acerca de viver com dignidade, nosso foco deveria estar
centrado nas nuanças de significado mais antigas. O que tenho em mente é viver
com a convicção de que nossa vida tem um valor intrínseco, de que possuímos o
potencial para uma excelência moral que ecoa o ritmo das estações e o hino
silencioso das galáxias. </span></p>

<p class=Normal>A busca consciente da dignidade não
desfruta de muita popularidade nos nossos dias, tendo sido comprimida por
concorrentes acirrados como a riqueza e o poder, o sucesso e a fama. Por trás
dessa depreciação da dignidade se encontram uma série de desenvolvimentos no pensamento
ocidental que surgiram como reação às certezas dogmáticas da teologia cristã. A
teoria Darwiniana da evolução, a teoria de Freud acerca do Id, o determinismo
econômico, a modelagem da mente por computador: todas essas tendências, que
surgiram mais ou menos de forma independente, atuaram em conjunto no sentido de
questionar a noção de que as nossas vidas têm mais valor do que os nossos
saldos bancários. Quando tantas pessoas seguras de si mesmas afirmam o
contrário, não encontramos mais justificativa para ver-nos como o ápice da
glória da criação. Ao invés disso, nos convencemos de que não somos nada mais
que blocos de protoplasma governados por genes egoístas, macacos espertos com
diplomas universitários e cartões de visita trafegando por auto-estradas ao
invés de árvores </span></p>


<p class=Normal>
Tais idéias,
distorcidas de várias formas, encontraram o seu caminho dos meios acadêmicos
para a cultura popular, corroendo a nossa noção de dignidade humana sob vários aspectos.
A economia de livre mercado, que ordena a moderna ordem social, indica o
caminho. Pois nesse sistema a principal forma de interação humana é o
investimento e o comércio, sendo que as pessoas são avaliadas simplesmente como
produtoras e consumidoras, às vezes até como produto de consumo. As nossas
vastas democracias despersonalizadas reduzem o indivíduo a um rosto sem nome no
meio da multidão, para ser manipulado através da propaganda, imagens e
promessas, para escolher desta ou daquela maneira. As cidades se expandiram
convertendo-se em enormes selvas urbanas, sujas e perigosas, cujos habitantes
confusos buscam uma fuga fácil através da ajuda de drogas e do sexo fácil.
Aumento da criminalidade, corrupção política, desentendimentos na vida em família,<span
style="mso-spacerun: yes">  </span>destruição do meio ambiente: tudo isso nos
diz muito acerca da forma deteriorada com que vemos a nós mesmos e como nos
relacionamos com os outros. <o:p></o:p></span></p>


<p class=Normal>
Cercado pelo
sofrimento das esperanças perdidas, poderá o Dhamma nos ajudar a recuperar a
nossa noção de dignidade e assim dar um novo significado para as nossas vidas?
A resposta a essa questão é sim, e de duas formas: primeiro, por justificar
nossa afirmação acerca de uma dignidade inata, e segundo, por nos mostrar o que
devemos fazer para revitalizar nosso potencial de dignidade. <o:p></o:p></span></p>


<p class=Normal>Para o Budismo, a dignidade inata dos seres humanos não provém da nossa relação com um deus todo poderoso ou por sermos dotados de uma alma imortal. Provém particularmente da posição privilegiada da vida humana dentro do vasto espectro de seres sensientes. Longe de reduzir os seres humanos a frutos do acaso, o Buda ensina que o reino humano é um reino muito especial estando exatamente no centro espiritual do cosmos. O que faz com que a vida humana seja tão especial é a capacidade de escolha moral que os seres humanos têm, e que não é compartilhada por nenhum outro tipo de ser. Embora essa capacidade esteja inevitavelmente sujeita a fatores limitantes, nós sempre possuímos, no momento presente, uma margem de liberdade interna que nos dá o poder de mudarmos a nós mesmos e assim, mudar o mundo. </p> 
<p class=Normal>No entanto, a vida no reino humano está longe de ser aconchegante. Pelo contrário, ela é inconcebivelmente difícil e complexa, abundante em conflitos e ambigüidades morais, oferecendo um enorme potencial para ambos, o bem e o mal. Essa complexidade moral pode converter a vida humana numa luta verdadeiramente dolorosa, mas ela também faz do reino humano o terreno mais fértil para plantar as sementes da Iluminação. Durante a longa jornada das nossas vidas, são esses entroncamentos ambíguos e perturbadores que nos oferecem a oportunidade de escolha entre nos elevarmos aos píncaros da grandeza espiritual ou cairmos até as profundezas mais degradantes. As duas alternativas se nos apresentam a cada momento e a escolha entre uma e outra depende unicamente de nós. </p>



<p class=Normal>
Apesar de essa
capacidade única para a escolha moral e o despertar espiritual conferirem uma
dignidade intrínseca à vida humana, o Buda não enfatiza tanto esse aspecto como
ele o faz com a nossa habilidade para adquirir dignidade ativa. Essa habilidade
está resumida em uma palavra que empresta o seu sentido para todo o conjunto
dos ensinamentos, <i style='mso-bidi-font-style:normal'>ariya</i> ou nobre. Os
ensinamentos do Buda são <i style='mso-bidi-font-style:normal'>ariyadhamma,</i>
a nobre doutrina, e o seu objetivo é mudar os seres humanos de “mundanos
ignorantes” para nobres discípulos resplandecentes com a nobre sabedoria. A
mudança não ocorre meramente através da fé e devoção mas trilhando o caminho do
Buda, que transforma as nossas fraquezas em forças invencíveis e nossa
ignorância em conhecimento. <o:p></o:p></span></p>


<p class=Normal>
A noção de
dignidade adquirida está intimamente ligada com a idéia de autonomia. Autonomia
significa autocontrole e autodomínio, libertação da inconstância da paixão e do
preconceito, a habilidade de ativamente determinar a si próprio. Viver com
dignidade significa ser senhor de si próprio: conduzir os seus assuntos de
acordo com o seu livre arbítrio ao invés de ser arrastado por forças que estão
fora do seu controle. O indivíduo autônomo obtém sua força de dentro de si
mesmo, livre das imposições do desejo e do preconceito, guiado pela sede de
justiça e pela percepção interna da verdade. <o:p></o:p></span></p>


<p class=Normal>
A pessoa que
representa o máximo da dignidade no Budismo é o arahant, o libertado, que
alcançou o cume da autonomia espiritual: libertação dos ditames da cobiça, raiva
e delusão. A própria palavra <i style='mso-bidi-font-style:normal'>arahant</i>
sugere essa noção de dignidade: a palavra significa “aquele que é digno”, que
merece a generosidade de devas e humanos. Embora na nossa condição atual ainda
possamos estar longe da estatura de um arahant, isso não significa que estamos
completamente perdidos pois os meios de alcançar esse objetivo mais elevado
estão ao nosso alcance. O meio é o Nobre Caminho Óctuplo com os seus dois
pilares do entendimento correto e conduta correta. Entendimento correto é o
primeiro fator do caminho e o guia para todos os demais. Viver com entendimento
correto significa ver que as nossas decisões importam, que nossas ações
volitivas têm conseqüências que vão além de si mesmas e que conduzem no longo
prazo à nossa felicidade ou sofrimento. A contrapartida ativa do entendimento
correto é a conduta correta, a ação guiada pelo ideal de excelente virtude e excelência
espiritual. Conduta correta com o corpo, linguagem e mente resulta na
realização dos demais sete fatores do caminho óctuplo, culminando no verdadeiro
conhecimento e libertação. <o:p></o:p></span></p>


<p class=Normal>
No mundo agitado
de hoje a humanidade se inclina de maneira irresponsável para duas direções
destrutivas. Uma é o caminho da luta e confrontação violenta, a outra é a
frívola entrega aos prazeres. Apesar de um aparente contraste, o que une esses
dois extremos é o desprezo pela dignidade humana: o primeiro viola a dignidade
de outras pessoas, enquanto que o último compromete a própria dignidade. O
Nobre Caminho Óctuplo do Buda é o caminho do meio que evita todos os extremos
prejudiciais. Seguir esse caminho não somente preenche a vida com uma calma
dignidade mas também responde ao cinismo da nossa época com uma afirmação
saudável.<o:p></o:p></span></p>

