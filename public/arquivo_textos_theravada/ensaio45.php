<p class=Normal>Na atualidade, a prática da meditação de insight (Vipassana) tem se
popularizado no mundo todo, mas ao obter esse sucesso a prática sofreu uma
sutil metamorfose. Ao invés de ser ensinada como parte integral do caminho
Budista, agora, ela tem sido apresentada com freqüência como uma disciplina
secular cujos frutos dizem respeito mais à vida no mundo do que à libertação
supramundana. Muitos meditadores são testemunhas dos benefícios tangíveis que
obtiveram com a prática da meditação de insight, benefícios que vão desde um
melhor desempenho profissional e melhoria no relacionamento até uma calma mais
profunda, mais compaixão e um nível de atenção mais aguçado. No entanto, embora
tais benefícios com certeza possuam os seus méritos, tomados em si mesmos estes
não são o objetivo final que o próprio Buda colocou como sendo o cume do seu
treinamento. Esse objetivo, colocado na terminologia dos textos, é a realização
de Nibbana, a destruição de todas as impurezas mentais aqui e agora e a libertação
do ciclo indeterminável de renascimentos.<o:p></o:p></span></p>

<p class=Normal>Talvez, a força mais
significativa que moldou a forma como a meditação de insight tem se manifestado
tenha sido a necessidade de transplantar a prática para um ambiente em grande parte
secular, distante da sua<span style='mso-spacerun:yes'>  </span>tradicional
matriz de fé e doutrina Budista. Dado o clima de ceticismo da nossa era, é
bastante conveniente que os principiantes no Dhamma tenham a oportunidade de
explorar por si mesmos o potencial inerente da prática. Talvez a última coisa
que eles precisem seja receber de golpe, desde o princípio, o conteúdo completo
da doutrina Budista. <o:p></o:p></span></p>

<p class=Normal>Entretanto, embora possamos
inicialmente começar a meditação com a mente aberta e exploradora, num certo
ponto da nossa prática inevitavelmente iremos nos deparar com uma encruzilhada
em que seremos forçados a fazer uma escolha. Ou prosseguimos com a meditação
como uma prática puramente naturalista, uma disciplina sem conotação religiosa,
ou poderemos transpor a prática de volta ao seu contexto original de fé e
compreensão Budistas. Se escolhermos o primeiro caminho, poderemos aprofundar
ainda mais a nossa meditação e obter em ainda maior abundância<span
style='mso-spacerun:yes'>  </span>os mesmos benefícios já obtidos até o
momento, uma calma ainda mais profunda, mais equanimidade, maior tolerância e
até mesmo algum tipo de penetração do aqui e agora. Todavia, mesmo que esses
benefícios sejam altamente desejáveis em si mesmos, comparados às palavras do
Buda eles permanecem incompletos. Pois, para que a prática da meditação de insight
atinja o pleno potencial descrito pelo Buda, ela deve ser suportada por várias
outras qualidades para que se encaixe dentro do contexto dos ensinamentos.<o:p></o:p></span></p>

<p class=Normal>Dentre essas qualidades a
mais importante é o par complementar da fé e entendimento correto. Como um
fator do caminho Budista, a fé <i>(saddha) </i>não significa a crença cega, mas
a disposição em aceitar com base na confiança algumas proposições que não somos
capazes de verificar por nós mesmos no atual estágio de desenvolvimento. Essas
proposições dizem respeito a ambos, a natureza da realidade e o alcance do
caminho. No esquema tradicional<span style='mso-spacerun:yes'>  </span>do
treinamento Budista, a fé é colocada no início como um pré-requisito para os
estágios subseqüentes que compreendem a tríade da virtude, concentração e
sabedoria. Os textos canônicos não parecem contemplar a possibilidade de que
uma pessoa carente de fé nos princípios doutrinários específicos do Dhamma
possa se dedicar à prática da meditação de insight e colher resultados
positivos. E no entanto, na atualidade, tal fenômeno tem se tornado amplamente
difundido. É bastante comum que os meditadores façam o seu primeiro contato com
o Dhamma através da prática intensiva de meditação de insight e depois usem
essa experiência como a pedra de toque para avaliar o seu relacionamento com os
ensinamentos.<o:p></o:p></span></p>

<p class=Normal>Nesse momento, a escolha
que eles fazem divide os meditadores em dois amplos grupos. O primeiro consiste
naqueles que focam exclusivamente nos benefícios tangíveis que a prática pode
proporcionar no aqui e agora, deixando de lado toda a preocupação com aquilo
que esteja além do horizonte da sua própria experiência pessoal. O outro
consiste naqueles que reconhecem que a prática flui de uma fonte de
entendimento muito mais profundo e amplo que o deles mesmo. Para seguir essa
sabedoria até a sua fonte, esses meditadores estão preparados a subordinar as
suposições bem conhecidas aos enunciados dos ensinamentos e dessa forma abraçar
o Dhamma na íntegra.<o:p></o:p></span></p>

<p class=Normal>O fato de que a meditação
de insight possa ser praticada com seriedade, mesmo fora do domínio da fé
Budista, levanta uma questão interessante que nunca foi colocada de forma
explícita no Cânone nem nos Comentários. Se a meditação de insight pode ser
praticada apenas visando os seus objetivos tangíveis imediatos, então qual o
papel que a fé desempenha no desenvolvimento do caminho? Com certeza, a fé,
sendo a plena aceitação da doutrina Budista, não é uma condição necessária para
a prática Budista. Como vimos, aqueles que não seguem o Dhamma como um caminho
para a emancipação espiritual ainda assim podem aceitar os preceitos éticos e
praticar a meditação como uma forma de obter paz.<o:p></o:p></span></p>

<p class=Normal>A fé, portanto, deve
desempenhar um papel distinto daquele de simplesmente impulsionar a ação, mas a
natureza exata desse papel ainda permanece problemática. Talvez, a solução
venha a emergir se perguntarmos o que na verdade significa a fé dentro do
contexto da prática Budista. Deve ser esclarecido de imediato que a fé não pode
ser explicada de forma adequada como sendo a simples reverência ao Buda, ou de
alguma liga de devoção, admiração e gratidão. Pois, apesar de que essas
qualidades com freqüência coexistem com a fé, todas elas podem estar presentes
mesmo estando a fé ausente.<o:p></o:p></span></p>

<p class=Normal>Se examinarmos a fé mais de
perto, veremos que além dos seus componentes emotivos, ela também compreende um
componente cognitivo. Este consiste em estar preparado a aceitar o Buda como o
único que descobriu e proclamou a verdade libertadora. Visto por esse ângulo, a
fé necessariamente implica uma decisão. A palavra decisão implica tomar um
partido, colocar a fé em algo é um exercício de discriminação. Portanto, a fé
Budista acarreta, no mínimo de forma implícita, a rejeição das reivindicações
de outros mestres espirituais de que eles são portadores de uma mensagem
libertadora em pé de igualdade com o próprio Buda. Sendo uma decisão, a fé
também implica a aceitação. Envolve a disposição de estar aberto aos princípios
anunciados pelo Iluminado e ater-se a eles como guias confiáveis para o
conhecimento e conduta.<o:p></o:p></span></p>

<p class=Normal>É essa decisão que separa
aqueles que se dedicam à prática da meditação de insight como uma disciplina
puramente naturalista daqueles que a praticam dentro do contexto da fé Budista.
A primeira, ao suspender qualquer julgamento acerca da descrição feita pelo
Buda sobre a condição humana, limita os frutos da prática àqueles compatíveis
com uma visão do mundo secular e naturalista. A última, ao aceitar a descrição
da condição humana feita pelo Buda, ganha acesso ao objetivo que o próprio Buda
anuncia como sendo a meta final da prática.<o:p></o:p></span></p>

<p class=Normal>O
segundo pilar que sustenta a prática da meditação de insight é a contraparte
cognitiva da fé, isto é, o entendimento correto <i>(samma dithi). </i>Embora a
palavra “entendimento” possa sugerir que os praticantes na verdade possuam a
experiência dos princípios que são considerados como corretos, isso ocorre
muito raramente no início do treinamento. Exceto para um número muito reduzido
de discípulos dotados, o entendimento correto no início significa na verdade a
crença correta, a aceitação dos princípios e doutrinas tendo por base a
confiança na iluminação do Buda. Embora Budistas modernistas algumas vezes
afirmem que o Buda disse que se deveria acreditar apenas naquilo que pode ser
verificado por si mesmo, nenhuma afirmativa desse tipo é encontrada no Cânone
em Pali. O que o Buda disse é que os ensinamentos dele não devem ser aceitos
cegamente mas que o seu significado deve ser investigado e que se deve tentar
realizar a verdade que eles contêm.<o:p></o:p></span></p>

<p class=Normal>Contrário
ao modernismo Budista, existem muitos princípios ensinados pelo Buda,
essenciais ao entendimento correto, que não podemos, no nosso estado atual, ver
por nós mesmos. De forma nenhuma são aspectos insignificantes, pois eles
definem a estrutura de todo o programa de libertação Budista. Não somente eles
descrevem as dimensões mais profundas do sofrimento do qual necessitamos nos
libertar, mas também apontam a direção onde a verdadeira libertação se encontra
e prescrevem os passos que conduzem à realização do objetivo.<o:p></o:p></span></p>

<p class=Normal>Esses
princípios incluem a crença em ambos, no entendimento correto “mundano” e no
“transcendente”. O entendimento correto mundano é aquele que conduz a um
destino feliz dentro do ciclo de renascimentos. Envolve a aceitação dos
princípios de kamma e os seus frutos; a aceitação da distinção entre ações hábeis
e inábeis; e a vasta extensão de reinos no samsara nos quais o renascimento
pode ocorrer. O entendimento correto transcendente é o entendimento que conduz
à completa libertação do samsara. Compreende o entendimento das Quatro Nobres
Verdades em suas ramificações mais profundas, oferecendo não um mero
diagnóstico do sofrimento psicológico mas uma descrição da escravidão <a
name=PVW>samsárica</a> e um programa para a libertação final. É esse
entendimento correto que se coloca no topo do Nobre Caminho Óctuplo e que guia
os demais sete fatores em direção à cessação do sofrimento. <o:p></o:p></span></p>

<p class=Normal>Embora
as técnicas para a prática da meditação de insight possam ser idênticas para
aqueles que a ela se dediquem como uma disciplina puramente naturalista e para
aqueles que a adotem como parte do desenvolvimento do Dhamma, os dois estilos
de prática irão no entanto diferir profundamente com respeito aos resultados
que essas técnicas possam produzir. Quando for praticada como parte de uma
abordagem naturalista, a meditação de insight poderá trazer uma maior calma,
compreensão e equanimidade, até mesmo experiências de insight. Ela poderá
purificar a mente das impurezas mais grosseiras e resultar na tranqüila
aceitação das vicissitudes da vida. Por essas razões, este tipo de prática não
deve ser depreciado. No entanto, sob uma perspectiva mais profunda, essa
apropriação da meditação Budista permanecerá incompleta. Ela ainda estará
confinada à esfera da existência condicionada, ainda estará vinculada ao ciclo
de kamma e aos seus frutos.<o:p></o:p></span></p>

<p class=Normal>Quando,
no entanto, a meditação de insight está sustentada pela profunda fé no Buda
como o perfeito mestre iluminado, e iluminada pela sabedoria dos ensinamentos,
ela adquire uma nova capacidade que falta à outra abordagem. Ela agora funciona
com o apoio do desapego, movendo-se em direção à libertação final. Ela se torna
a chave para abrir as portas para o Imortal, o meio para obter a liberdade que
nunca poderá ser perdida. Assim, a meditação de insight transcende os limites
do condicionado, transcende até mesmo a si mesma para alcançar o seu objetivo:
a erradicação de todos os grilhões da existência e a libertação do ciclo
indeterminável de nascimento, envelhecimento e morte.<o:p></o:p></span></p>

