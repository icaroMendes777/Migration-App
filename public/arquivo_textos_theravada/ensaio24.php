<p class=Normal>Na
atualidade, todas as principais religiões do mundo têm que responder a um duplo
desafio. De um lado está o desafio da secularização, uma tendência que varreu o
mundo, demolindo as cidadelas mais antigas daquilo que é sagrado e convertendo
todas as iniciativas do homem em direção ao Além num gesto inútil, comovente
mas desprovido de sentido. De outro lado está o encontro das grandes religiões.
À medida que as nações e culturas mais remotas se fundem numa única comunidade
global, os representantes da busca pela espiritualidade humana acabaram sendo
reunidos num foro de uma intimidade sem precedentes, um encontro tão próximo
que não deixa espaço para recuo. Assim, cada uma das principais religiões
enfrenta, à sua vez e ao mesmo tempo, no anfiteatro da opinião pública mundial,
todas as demais religiões da terra, bem como o vasto número de pessoas que
reagem com desdém, ou com um bocejo indiferente, a todos aqueles que
reivindicam possuir a Grande Resposta. <o:p></o:p></span></p>

<p class=Normal>Nesta situação, qualquer
religião que queira emergir como algo mais do que uma relíquia da adolescência
da humanidade tem que ter a capacidade de lidar, de forma convincente e
concreta, com os dois lados do desafio. De um lado ela tem que conter a
crescente onda de secularização, mantendo viva a intuição de que nenhum nível
de controle tecnológico sobre a natureza, nemhum grau de habilidade em
satisfazer as necessidades mundanas da humanidade poderá trazer paz para o
espírito humano ou poderá saciar a sede por uma verdade e valores que
transcendam as fronteiras da contingência. Por outro lado, cada religião tem
que encontrar alguma forma de desembaraçar as afirmações conflituosas que todas
religiões fazem, de entender o nosso lugar no esquema mais amplo das coisas e
de possuir a chave para a nossa salvação. Enquanto permanece firme nos seus
princípios mais fundamentais, uma religião tem que ser capaz de tratar as
diferenças significativas entre a sua própria doutrina e aquela dos outros
credos, de uma forma tal que seja ao mesmo tempo, honesta e no entanto humilde,
perspicaz mas sem se impor.<o:p></o:p></span></p>

<p class=Normal>Neste breve ensaio eu
gostaria de delinear uma resposta adequada Budista para o segundo desafio.
Visto que o Budismo sempre declarou oferecer um &quot;caminho do meio&quot;
para a solução dos dilemas intelectuais e éticos da vida espiritual, podemos
descobrir que a chave para a nossa problemática atual também se encontra em
descobrir a resposta que melhor exemplifique o caminho do meio. Como tem sido
observado com freqüência, o caminho do meio não é um compromisso entre os
extremos mas um caminho que se eleva acima deles, evitando as armadilhas às
quais cada um deles conduz. Portanto, ao buscar a abordagem Budista adequada ao
problema da diversidade de credos, podemos começar apontando os extremos que o
caminho do meio deve evitar. <o:p></o:p></span></p>

<p class=Normal>O primeiro extremo é um
recuo ao fundamentalismo, a proclamação agressiva daquilo que a pessoa crê
combinada com o proselitismo fervoroso para com aqueles que ainda se encontram
fora do círculo dos correligionários. Enquanto que essa reposta ao desafio da
diversidade tem assumido proporções alarmantes nas congregações de fiéis das
grandes religiões monoteístas, Cristianismo e Islamismo, não é o tipo de
resposta em relação à qual o Budismo possua uma afinidade imediata, pois as
diretrizes éticas do Dhamma tendem naturalmente a estimular uma atitude de
tolerância benevolente em relação a outras religiões e aos seus seguidores.
Embora não possa haver uma garantia contra o surgimento de uma militância
ligada ao<span style="mso-spacerun: yes">  </span>fundamentalismo dentro das
próprias fileiras do Budismo, os ensinamentos do Buda não oferecem nenhuma
purificação, nem mesmo a mais remota, para tal desenvolvimento malígno. <o:p></o:p></span></p>

<p class=Normal>Para os Budistas a
alternativa mais tentadora é o segundo extremo: Este extremo, que alcança a
tolerância ao custo da integridade, pode ser chamado de a tese do
espiritualismo universal: a opinião de que todas as grandes religiões no seu
núcleo sustentam essencialmente a mesma verdade, apenas vestidas de distintas
formas de expressão. Tal tese não poderia, é claro, ser mantida em relação aos
credos formais das principais religiões, que diferem tanto que seria necessário
um esforço tenaz de contorção de palavras para lograr um acordo. Ao invés
disso, chega-se à posição universalista por um caminho indireto. Os seus
defensores argumentam que precisamos distinguir entre a face externa de uma
religião - as suas crenças explícitas e práticas exotéricas - e o seu núcleo
interno de realização experimental. Baseado nesta distinção, eles então
insistem que por detrás das significativas diferenças externas entre as grandes
religiões, na sua essência – com relação à experiência espiritual da qual elas
emergem e o objetivo último ao qual elas conduzem – descobriremos<span
style="mso-spacerun: yes">  </span>que elas são substancialmente idênticas.
Portanto as principais religiões diferem apenas na medida em que são meios
distintos, expedientes diferentes para a mesma experiência libertadora, que
pode ser indiscriminadamente designada como &quot;iluminação&quot;, ou
&quot;redenção&quot;, ou &quot;realização divina&quot;, já que esses diferentes
termos simplesmente designam diferentes aspectos do mesmo objetivo. Como diz o
famoso provérbio: os caminhos que conduzem ao topo da montanha são muitos, mas
o lugar no topo é um só. Sob esse ponto de vista, o Dhamma do Buda é somente
uma outra variação da &quot;filosofia perene&quot; subjacente a todas as
expressões maduras de busca espiritual do homem. O Dhamma pode se destacar por
sua elegante simplicidade, claridade e objetividade; mas uma revelação única e
exclusiva da verdade ele não contém. <o:p></o:p></span></p>

<p class=Normal>À primeira vista, a adoção
de tal idéia parece ser um passo indispensável na direção da tolerância
religiosa, e a insistência em que as diferenças doutrinárias não são apenas
verbais mas reais e importantes pode dar a impressão de beirar a intolerância.
Dessa forma aqueles que adotam o Budismo como uma reação contra a estreiteza
doutrinária das religiões monoteístas podem encontrar nessa idéia - tão suave e
amoldável - uma bem vinda trégua à típica insistência dessas religiões de terem
acesso privilegiado à verdade. No entanto, um estudo imparcial dos discursos do
Buda mostra de forma muito clara que a tese universalista não conta com o
endosso do próprio Abençoado. Ao contrário, o Buda repetidamente proclama que o
caminho para o objetivo supremo da vida santa é conhecido apenas através dos
seus próprios ensinamentos e portanto a realização desse objetivo - a completa
libertação do sofrimento - somente pode ser alcançada através da sua própria
revelação. A mais conhecida instância desta afirmação foi na véspera do seu <a
name=PVW>Parinibbana</a>: que somente no seu ensinamento são encontrados os
quatro tipos de pessoas iluminadas e que as outras seitas são desprovidas de
verdadeiros ascetas, aqueles que alcançaram os planos de libertação. <o:p></o:p></span></p>

<p class=Normal>A restrição do Buda de que
a emancipação final apenas faça parte da sua revelação não resulta de um
dogmatismo estreito ou da falta de boa vontade, mas está apoiada sobre uma
determinação absolutamente precisa da natureza do objetivo final e do método
que precisa ser implementado para alcançá-lo. Esse objetivo não é nem a vida
eterna num paraíso, nem a concepção nebulosa de um estado de iluminação
espiritual, mas o elemento de Nibbana, a libertação sem deixar nenhum resíduo
do ciclo de repetidos nascimentos e mortes. Esse objetivo é realizado com a
completa destruição das impurezas mentais - desejo, raiva e delusão - até o seu
nível mais sutil de latência. A erradicação das impurezas somente pode ser
alcançada através do insight da verdadeira natureza dos fenômenos, o que significa
que alcançar Nibbana depende do insight, através da experiência direta, de
todos os fenômenos condicionados, internos e externos, marcados com as
&quot;três características da existência&quot;: impermanentes, insatisfatórios
e não-eu. O que o Buda sustenta, como fundamento para a sua declaração de que
os seus ensinamentos oferecem a única maneira de obter a libertação final do
sofrimento, é que o conhecimento da verdadeira natureza dos fenômenos, com toda
exatidão e integridade, só pode ser alcançado através dos seus ensinamentos.
Assim é porque, em teoria, os princípios que definem esse conhecimento são
exclusivos dos seus ensinamentos e contraditórios em aspectos vitais em relação
às doutrinas básicas de outras crenças; e porque na prática, somente os seus
ensinamentos revelam, na sua perfeição e pureza, o método para gerar esse
entendimento libertador como um elemento da experiência pessoal imediata. Esse
método é o Nobre Caminho Óctuplo que, como um método de treinamento espiritual
integrado, não pode ser encontrado fora da revelação de um Iluminado. <o:p></o:p></span></p>

<p class=Normal>Surpreendentemente, essa
postura exclusivista do Budismo em relação à possibilidade de obter a
emancipação final nunca engendrou uma política de intolerância por parte dos Budistas
em relação aos devotos de outras religiões. Ao contrário, durante a sua longa
história, o Budismo tem demonstrado profunda tolerância e uma cordial boa
vontade em relação às muitas religiões com as quais esteve em contato. Essa
tolerância tem sido mantida simultaneamente com a profunda convicção de que a
doutrina do Buda oferece o único e insuperável caminho para a libertação dos
problemas inerentes da existência condicionada. Para o Budismo, a tolerância
religiosa não é alcançada reduzindo todas as religiões a um denominador comum,
nem explicando as formidáveis diferenças nas idéias e práticas como acidentes
do desenvolvimento histórico. Do ponto de vista Budista, fazer com que a
tolerância seja contingente para encobrir discrepâncias não seria um exercício
de verdadeira tolerância; pois tal abordagem só &quot;tolera&quot; diferenças
através da diluição delas, de forma tão completa que já não fazem qualquer
diferença. A verdadeira tolerância religiosa envolve a capacidade de admitir
diferenças reais e fundamentais, mesmo que profundas e irreconciliáveis, e
apesar disso, respeitar os direitos daqueles que seguem uma religião diferente
da sua própria (ou nenhuma religião) e continuar a agir assim sem
ressentimento, desvantagens ou restrições. <o:p></o:p></span></p>

<p class=Normal>A tolerância Budista brota
do reconhecimento de que o caráter e as necessidades espirituais dos seres
humanos são demasiadamente diversos para serem abrangidos por uma doutrina em
particular, e que portanto essas necessidades irão naturalmente encontrar uma
forma de expressão em uma ampla variedade de formas religiosas. Os sistemas não
Budistas não serão capazes de conduzir os seus devotos ao objetivo final do
Dhamma do Buda, mas isto, de todos modos nunca foi a proposta original delas.
Para o Budismo, a aceitação da idéia de um ciclo de renascimentos, sem um
princípio definido, significa que seria completamente irreal esperar que mais
que um pequeno número de pessoas fossem atraídas para um caminho espiritual
direcionado para a completa libertação. A maioria esmagadora, mesmo aqueles que
buscam a emancipação das desgraças mundanas, terá como objetivo assegurar um
meio de existência favorável dentro do ciclo, ao mesmo tempo que confundem isso
com o objetivo último da busca religiosa. <o:p></o:p></span></p>

<p class=Normal>Na medida em que uma
religião proponha princípios éticos sólidos e possa promover o desenvolvimento
de qualidades benéficas tais como o amor, a generosidade, o desapego e a
compaixão, ela irá merecer nesses aspectos a aprovação dos Budistas. Esses
princípios defendidos por outros sistemas religiosos também conduzem ao
renascimento nos reinos de bem aventurança - os paraísos celestiais e as moradas
divinas. O Budismo não reivindica de nenhuma forma deter acesso único a esses
reinos, mas sustenta que os caminhos que conduzem a eles foram articulados, com
distintos graus de clareza, em muitas das grandes tradições espirituais da
humanidade. Ao mesmo tempo que o Budista irá discordar da estrutura de crenças
de outras religiões, na medida em que elas se desviem do Dhamma do Buda, ele as<span
style="mso-spacerun: yes">  </span>respeitará na medida em que elas imponham
virtudes e padrões de conduta que promovam o desenvolvimento espiritual e a
integração harmoniosa dos seres humanos entre si e com o mundo. <o:p></o:p></span></p>


