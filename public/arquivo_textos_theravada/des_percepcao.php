<p class=Normal>A meditação
nos ensina sobre o poder das nossas percepções. Passamos a ver como os rótulos
que aplicamos às coisas, as imagens com as quais visualizamos as coisas, têm
uma enorme influência naquilo que vemos e como isso pode nos oprimir com
sofrimento e estresse. À medida que a meditação evolui, no entanto, ela
proporciona as ferramentas que necessitamos para nos libertarmos dessa
influência.<o:p></o:p></span></p>

<p class=Normal>No início, quando você se
dá conta pela primeira vez do poder da percepção, você pode facilmente se
sentir oprimido pelo grau de abrangência que ele possui. Suponha que você
esteja focado na respiração. Chega um ponto em que você irá se perguntar se o
seu foco está na respiração em si ou na <i>idéia </i>da respiração. Uma vez que
esta questão surja, a reação normal é tentar contornar a idéia em busca da
sensação crua que está por trás da idéia. Mas se você for realmente sensível ao
fazer isso, irá observar que está simplesmente substituindo uma caricatura da
respiração por outra um tanto mais sutil. Até mesmo a sensação crua da
respiração estará moldada pela forma como você conceitua a sensação crua. Não
importa o quanto você se esforce para identificar com exatidão uma experiência
da respiração que não tenha sido filtrada, ainda assim você notará que ela
estará moldada pela sua idéia do que é na verdade a respiração. Quanto mais
você perseguir a realidade da respiração, mais ela aparecerá como uma miragem. <o:p></o:p></span></p>

<p class=Normal>O truque, nesse caso, é
reverter esse fato em nosso benefício. Afinal, você não está meditando para
chegar até a respiração. Você está meditando para compreender os processos que
conduzem ao sofrimento para poder dar um fim neles. A maneira como você se
relaciona com as suas percepções é parte desses processos, estão <i>é</i> <i>isso
</i>que você quer ver. Você tem que tratar a sua experiência com a respiração
não como um fim em si mesmo, mas como uma ferramenta para compreender o papel
da percepção na criação do sofrimento e estresse. <o:p></o:p></span></p>

<p class=Normal>Você faz isso através da
des-percepção: questionando as suas suposições acerca da respiração, mudando
essas suposições de forma deliberada e observando o que acontece como
resultado. Agora, sem o contexto apropriado, a des-percepção pode com
facilidade se perder em abstrações aleatórias. Então você adota a prática da
concentração como contexto, provendo a des-percepção tanto com um rumo geral,
como com tarefas específicas que a forçam a confrontar as premissas operativas
que na realidade moldam a sua experiência do presente.<o:p></o:p></span></p>

<p class=Normal>O rumo geral está em tentar
trazer a mente para níveis cada vez mais profundos e duradouros de
tranqüilidade, eliminando assim níveis cada vez mais sutis de estresse. Você
não estará tentando verificar quais percepções da respiração caracterizam-na de
modo mais verdadeiro, mas, sim, quais funcionam melhor e em que situações, para
eliminar o estresse. A objetividade que você está buscando não é a objetividade
da respiração, mas a objetividade da causa e efeito. <o:p></o:p></span></p>

<p class=Normal>As tarefas específicas que
ensinam essas lições principiam com a tarefa de tentar fazer com que a mente
fique focada confortavelmente por longos períodos de tempo na respiração – e
nesse ponto exato você se depara com duas premissas operativas: O que significa
respirar? O que significa permanecer focado?<o:p></o:p></span></p>

<p class=Normal>Em geral, pensa-se na
respiração como o ar que passa entrando e saindo pelo nariz e essa pode ser uma
percepção útil como ponto de partida. Use qualquer sensação mais evidente que
você associe com essa percepção como meio para estabelecer a atenção plena,
desenvolver a plena consciência e fazer com que a mente fique cada vez mais
tranqüila. Mas à medida que a sua atenção ficar mais refinada, você poderá
achar o nível da respiração demasiado débil para ser detectado. Então, ao invés
disso, tente pensar na respiração como o fluxo de energia no corpo, como um
processo corporal completo. <o:p></o:p></span></p>

<p class=Normal>Em seguida, faça com que
essa experiência seja tão confortável quanto possível. Se você sentir algum
bloqueio ou obstrução na respiração, veja o que pode ser feito para dissolver
essas sensações. Você está fazendo algo para criá-las? Se você for capaz de
pegar a si mesmo criando-as, então será mais fácil dissolvê-las. E o que faria
você criá-las, fora as suas noções preconcebidas de como deve operar a mecânica
da respiração? Então, questione essas noções: Onde a respiração penetra no
corpo? Ela entra só através do nariz e da boca? O corpo tem que puxar a
respiração para dentro? Se for assim, quais sensações realizam o puxão? Quais
sensações são puxadas? Onde começa o puxão? E de onde é puxada a respiração?
Quais partes têm a respiração e quais não têm? Ao sentir uma sensação de
bloqueio, de qual lado da sensação você se encontra? <o:p></o:p></span></p>

<p class=Normal>Essas questões podem
parecer estranhas, mas muitas vezes as nossas concepções pré-verbais sobre o
corpo também são muito estranhas. Só quando você as confronta com perguntas
estranhas é que poderá trazê-las à tona. E só quando puder vê-las claramente é
que poderá substituí-las por conceitos alternativos. <o:p></o:p></span></p>

<p class=Normal>Então, assim que você se
pegar respirando de modo desconfortável de acordo com uma certa suposição em
particular, vire-a para a direção oposta para ver quais sensações essa nova
suposição irá destacar. Tente permanecer com essas sensações por tanto tempo
quanto puder, para testá-las. Se, comparadas com as suas sensações anteriores
associadas à respiração, for mais fácil permanecer com essas novas sensações,
se elas proporcionarem uma base mais sólida e ampla para a concentração, a
suposição que as chamou para a sua atenção passa a ser uma nova ferramenta útil
para a sua meditação. Se as novas sensações não forem úteis nesse sentido, você
poderá deixar essa nova ferramenta de lado. <o:p></o:p></span></p>

<p class=Normal>Por exemplo, se você tem a
impressão de estar de um lado de um bloqueio, tente pensar que está do outro
lado. Tente estar em ambos os lados. Pense na respiração vindo para dentro do
corpo, não através do nariz ou boca, mas através do meio do peito, pela parte
detrás do pescoço, através de cada poro da sua pele, de qualquer ponto que
ajude a reduzir<span style="mso-spacerun: yes">  </span>essa necessidade de
puxar e empurrar.<o:p></o:p></span></p>

<p class=Normal>Ou passe a questionar a
necessidade de empurrar e puxar. Você sente que a sua experiência imediata do
corpo corresponde às suas partes sólidas e de que estas têm que operar a
mecânica da respiração que é secundária? O que acontece se você conceber a sua
experiência imediata do corpo de uma forma distinta, como um campo de energia
primária da respiração e a solidez como um simples rótulo anexado a certos
aspectos da respiração? O que quer que seja que você experimente como uma
sensação corporal primária, pense nisso já como a respiração, sem ter que fazer
nada mais para isso. Como isso afeta o nível de estresse e tensão na
respiração? <o:p></o:p></span></p>

<p class=Normal>E sobre o ato de permanecer
focado? Qual a sua concepção disso? E ela está por trás da respiração?
Circundada pela respiração? Até que ponto o seu quadro mental daquilo que é
focar ajuda ou atrapalha a facilidade e a solidez da sua concentração? Por
exemplo, você poderá achar que pensa que a mente está numa parte do corpo mas
não em outras. O que acontece quando você foca a atenção em outra parte? A
mente tem que deixar a sua base de operações – digamos, na cabeça – para ir até
lá, ou a outra parte tem que ser trazida até a cabeça? Que tipo de tensão isso
cria? O que acontece se você pensar que a consciência já está presente naquela
outra parte? O que acontece se você der uma reviravolta em tudo: ao invés da mente
estar no corpo, veja quanto estresse é eliminado ao pensar que o corpo está
circundado por um campo de consciência preexistente. <o:p></o:p></span></p>

<p class=Normal>Ao fazer perguntas desse
tipo e obter resultados favoráveis, a mente poderá se estabelecer em níveis
cada vez mais profundos de solidez. Você elimina a tensão e estresse
desnecessários do seu foco, encontrando formas de se sentir cada vez mais em
casa, tranqüilo, experimentando o presente. <o:p></o:p></span></p>

<p class=Normal>Uma vez que a mente tenha
se acalmado, conceda-lhe tempo para que assim permaneça. Não tenha pressa de
seguir adiante. Neste ponto as questões são, “Quais partes do processo requerem
foco? Quais podem ser abandonadas? Quais devem ser mantidas para manter este
foco?” Sintonizar o nível correto de atenção é um processo; manter isso é
outro. Quando você aprender como manter o seu senso de quietude, tente mantê-lo
ativo em todas as situações. Você descobre o que é que atrapalha? É a sua
própria resistência às perturbações? Você é capaz de fazer com que a sua
quietude seja tão porosa que as perturbações a atravessem sem colidir com nada,
sem desequilibrá-lo? <o:p></o:p></span></p>

<p class=Normal>À medida que você for
ficando mais absorto na exploração desses temas, a concentração se tornará
menos uma batalha contra as perturbações e mais uma oportunidade para a
exploração interior. E sem nem mesmo pensar nelas, você estará desenvolvendo as
quatro bases para o sucesso: o desejo de compreender as coisas, a persistência
que o mantém na sua investigação, a atenção cuidadosa que você presta à causa e
efeito, e a perspicácia empregada por você na formulação das questões. Todas
essas qualidades contribuem para a concentração, auxiliam no seu
estabelecimento, solidez e clareza. <o:p></o:p></span></p>

<p class=Normal>Ao mesmo tempo elas
estimulam o discernimento. O Buda certa vez disse que um teste para o
discernimento de uma pessoa é a maneira pela qual ela formula as suas questões
e tenta respondê-las. Assim, para estimular o discernimento, você não pode
simplesmente se apegar a instruções predefinidas para a sua meditação. Você tem
que a praticar a formulação de questões e testar o <a name=PVW>karma</a> destas
investigando o seu resultado. <o:p></o:p></span></p>

<p class=Normal>Por fim, quando você chegar
a uma percepção da respiração que permita as sensações da inspiração e
expiração se acalmarem, você poderá começar a questionar percepções mais sutis
do corpo. É como sintonizar uma estação de rádio. Se o seu receptor não estiver
sintonizado com precisão, a estática irá interferir com a sutileza daquilo que
estiver sendo transmitido. Mas quando você estiver perfeitamente sintonizado,
todas as nuanças serão percebidas. O mesmo ocorre com as sensações do corpo:
quando os movimentos da respiração se acalmam, as nuanças mais sutis de como a
percepção interage com as sensações físicas passam a se destacar. O corpo
parece uma névoa de sensações atômicas e você começa a ver como as suas percepções
interagem com essa névoa. Até que ponto a forma do corpo é inerente a essa
névoa? Até que ponto é algo intencional – algo adicionado? O que acontece se
você abandonar a intenção de criar essa forma? Você é capaz de focar no espaço
entre as gotículas nessa névoa? O que acontece então? Você é capaz de
permanecer nisso? O que acontece quando você abandona a percepção do espaço e
foca no conhecimento? Você é capaz de permanecer nisso? O que acontece quando
você abandona a unicidade do conhecimento? Você é capaz de permanecer nisso? O
que acontece quando você tenta abandonar a rotulação de absolutamente tudo?<o:p></o:p></span></p>

<p class=Normal>À medida que você se
estabelece nesses estados desprovidos de forma, é importante que você não perca
de vista o seu propósito ao sintonizá-los. Você está ali para entender o
sofrimento, não para super-interpretar aquilo que você experimenta. Digamos,
por exemplo, que você se estabeleça num senso envolvente de espaço ou
consciência. A partir disso, é fácil assumir que você alcançou a consciência
primordial, a base do ser, do qual todas as coisas emergem, para a qual todas
retornam e que é essencialmente intocada por todo o processo de emersão e
regresso. Você poderá tomar as descrições do Incondicionado e aplicá-las àquilo
que estiver experimentando. Se você estiver estabelecido num estado de nem
percepção, nem não percepção, é fácil ver isso como o não-permanecer,
desprovido de diferenciação entre aquele que percebe e o percebido, pois a
atividade mental é tão atenuada sendo virtualmente imperceptível. Impressionado
com a aparente ausência de esforço desse estado, você poderá sentir que superou
a paixão, aversão e delusão simplesmente por considerá-las como irreais. Se
você aderir a esse tipo de concepção poderá facilmente imaginar ter alcançado o
fim do caminho antes de ter na realidade concluído o seu trabalho. <o:p></o:p></span></p>

<p class=Normal>A sua única proteção neste
caso é considerar essas concepções como formas de percepção e também
desmantelá-las. E é neste ponto que as quatro nobres verdades provam o seu
valor como ferramentas para desmantelar qualquer concepção detectando o
estresse que as acompanha. Questione se ainda existe algum estresse sutil na
concentração que passou a ser o seu local de permanência. O que acompanha esse
estresse? Quais movimentos errantes da mente o estão criando? Quais movimentos
persistentes da mente o estão criando? Você tem que estar vigilante em relação
a ambos. <o:p></o:p></span></p>

<p class=Normal>Dessa
forma, você ficará face a face com as percepções que mantêm até mesmo os
estados de concentração mais sutis. E você verá que até mesmo <i
style='mso-bidi-font-style:normal'>essas </i>são estressantes. Se você
substituí-las por outras percepções, no entanto, você simplesmente estará
trocando um tipo de estresse por outro. É como se os níveis ascendentes de
concentração o levassem até o topo de um pau de bandeira. Você olha para baixo
e vê<span style="mso-spacerun: yes">  </span>envelhecimento, enfermidade e
morte subindo no pau em perseguição. Você exauriu todas as possibilidades que a
percepção pode oferecer, então o que irá fazer? Você não pode simplesmente
ficar onde está. A sua única opção é soltar- se. E se você se soltar de tudo,
irá se soltar da gravidade também.<o:p></o:p></span></p>

