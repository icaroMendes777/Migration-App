<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O Buda não
era o tipo de mestre que apenas respondia às perguntas. Ele também ensinava o
que perguntar. Ele compreendia o poder das perguntas: elas formatam a nossa
ignorância e forçam esse formato, válido ou não, sobre as respostas que,
esperamos, darão um fim à nossa ignorância. Mesmo que usemos uma informação
correta para responder a uma pergunta incorreta, aquela poderá assumir um
formato incorreto. Assim, se você usar essa resposta como uma ferramenta, ela
com certeza será aplicada nas situações incorretas, obtendo resultados
equivocados.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>É por isso
que o Buda foi muito cuidadoso ao mapear um sistema de perguntas, mostrando
quais perguntas, em que ordem, conduzem à libertação e quais não conduzem. Ao
mesmo tempo, ele proferia os seus discursos sob a forma de perguntas e
respostas, de modo a deixar perfeitamente claro qual o formato das questões que
ele estava respondendo.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Portanto,
se você procurar respostas nos ensinamentos e quiser obter o máximo delas, você
deveria, primeiro, ter bem claro em mente que perguntas formular e verificar se
elas estão de acordo com as perguntas que os ensinamentos pretendem responder.
Assim, as respostas não o colocarão na direção equivocada.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Um exemplo
que suporta esse argumento é o ensinamento sobre o não-eu. Muitos estudantes
interpretam esse<span style="mso-spacerun: yes">  </span>ensinamento como a
resposta do Buda a duas das questões perguntadas com mais freqüência na
história do pensamento crítico: “Quem sou eu?” e “Tenho um verdadeiro eu?” Sob
a luz dessas questões, o ensinamento aparenta ser uma doutrina sobre a não
existência de um eu dizendo ou um Não sem restrições: não existe um eu; ou um
Não com restrições: não existe um eu <i>separado. </i>Mas na única vez em que o
Buda foi perguntado de forma direta se existe um eu, ele se recusou a
responder, fundamentado em que uma resposta Sim ou Não à pergunta conduziria a
formas extremas de entendimento incorreto que bloqueariam o caminho para a
libertação. Um Sim ou um Não com restrições conduziriam ao apego: você ficaria
apegado à noção de um eu, não<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>importa
como você definisse isso. Um Não sem restrições conduziria à confusão e
alienação, pois você sentiria que a sua noção mais profunda de auto estima
teria sido negada.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quanto à
questão, “Quem sou eu?” o Buda a incluiu numa lista de perguntas que conduzem a
um beco sem saída que levam a “um emaranhado de idéias, uma confusão de idéias,
uma contorção, uma entortadura, um grilhão de idéias. Aprisionado pelo grilhão
de idéias, vocês não irão obter a libertação do nascimento, envelhecimento e morte,
da tristeza, lamentação, dor, angústia ou desespero.” Em outras palavras, todo
o esforço para responder a esse tipo de questão é karma inábil, que bloqueia o
caminho para a verdadeira libertação.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Portanto,
se o ensinamento sobre o não-eu não tem a intenção de responder a essas
questões, quais questões ele <i>sim </i>responde? Uma questão básica é: “O que
é hábil?” Na verdade, <i>todos </i>os<i> </i>ensinamentos do Buda são respostas
diretas ou indiretas a essa questão. O grande insight que ele obteve foi que
todo o nosso conhecimento e ignorância, todo nosso prazer e dor, têm origem nas
nossas ações, nosso karma, portanto, a busca pelo verdadeiro conhecimento e
verdadeira felicidade se resumem numa questão de habilidade. Neste caso, a
pergunta precisa é : “A identificação com um eu é hábil?” E a resposta é:
“Apenas até certo ponto.” Nas áreas em que você necessite de uma noção saudável
de um eu para agir com habilidade é sábio manter essa noção. Mas com o tempo, à
medida que o comportamento hábil se transforme num hábito e você
desenvolva<span style="mso-spacerun: yes">  </span>mais sensibilidade, verá que
a identificação com um eu, mesmo do tipo mais sutil, é prejudicial e
estressante. Você tem que se soltar dela.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, tal
como com uma outra habilidade qualquer, existem passos definidos ao longo do
caminho para se obter a maestria. E visto que, fazer uma pergunta é um tipo de
karma, as perguntas que você fizer devem não somente começar com o tema da
habilidade, mas elas devem também ser hábeis em si mesmas, abordar o tema com
habilidade. Cada passo na habilidade do Buda é portanto definido por uma série
de questões que focam a sua atenção e moldam a sua forma de pensar na direção
mais estratégica. Na verdade, as perguntas que ele recomenda podem ser tomadas
como um mapa para a prática: você começa com perguntas que presumem um eu e
usam essa pressuposição para motivá-lo a agir de<span style="mso-spacerun:
yes">  </span>forma cada vez mais hábil. Somente quando você alcança um nível
apropriado de habilidade as perguntas passam a desmontar a sua noção do eu,
selecionando as coisas que você identifica como sendo o seu eu e mostrando que
elas na verdade não o são. Quando a ação de auto-identificação esgota os locais
onde possa aportar, ela fica suspensa no ar – e a mente se abre para a
libertação. Portanto, se você colocar o ensinamento sobre o não-eu no seu
contexto correto – esse sistema de perguntas – verá que não se trata de uma
resposta estéril para uma questão estéril. Ao invés disso, é uma ferramenta de
vanguarda para produzir a libertação.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Para
iniciar esse sistema, o Buda recomenda que ao visitar um mestre, as primeiras
perguntas que você deve fazer são estas: “O que é hábil? O que é inábil? O que,
se eu fizer, trará meu sofrimento e dano por um longo tempo? Ou o que, se eu
fizer, trará minha felicidade e bem estar por um longo tempo?” Embora essas
duas últimas perguntas contenham os conceitos de “eu” e “meu,” estes não são o
foco da questão. O foco está na ação, no desenvolvimento da habilidade, em usar
a sua preocupação com o “eu” e “meu bem estar” para treinar as suas ações para
que elas se dirijam para a verdadeira felicidade.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>As
respostas do Buda para essas questões preliminares se parecem a um curso de
sobrevivência na selva. Primeiro vem o que deve e o que não deve ser feito. Um instrutor
de selva lhe diria: “Se um alce vier no seu encalço, corra. Se um urso vier no
seu encalço, não corra.” As instruções equivalentes do Buda sobre o que fazer e
o que não fazer são dez regras que dizem respeito ao corpo, linguagem e mente.
As regras para o corpo são: não mate, não roube, não pratique atos sexuais impróprios.
Para a linguagem: não fale mentiras, não fale com malícia, não fale de forma grosseira,
não fale frivolidades. E para a mente: abandone a cobiça, abandone a má
vontade, cultive o entendimento correto. Essas são as regras básicas do Buda
para a sobrevivência da sua felicidade e muitos dos seus ensinamentos
simplesmente elaboram sobre esses dez pontos.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Mas
qualquer instrutor de selva lhe dirá que a sobrevivência exige mais do que
essas regras gerais. Você tem que estar alerta para as lacunas não cobertas
pelas regras. Você tem que aprender a usar os seus poderes de observação,
imaginação e perspicácia para desenraizar hábitos inábeis e desenvolver novos
hábitos para preencher esses espaços. Dessa forma você será capaz de viver com
conforto na selva, respeitando os alces e os ursos e outros perigos que se
encontrem à sua volta sem ser derrotado por eles.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O mesmo se
aplica à habilidade do Buda: além de seguir as regras sobre o que deve e não
deve ser feito, você precisa aprender a desenterrar as raízes do comportamento
inábil de forma que você possa se tornar um perito em todas as áreas da sua
vida, incluindo aquelas áreas onde as regras sobre o que deve e não deve ser
feito não se apliquem. As raízes do comportamento inábil são três: cobiça,
raiva e delusão. Dessas três, a delusão é a mais insidiosa, pois quando você
está deludido você não <i>sabe </i>que está deludido. A única forma de super­á-la
é sendo um observador incansável, olhando para as suas ações em termos de causa
e efeito, avaliando as suas conseqüências no curto e longo prazo para si mesmo
e para os outros. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Outra vez,
isto envolve aprender a fazer as perguntas corretas. Cada vez antes de agir,
pergunte a si mesmo: 'Esta ação que quero praticar: conduzirá à minha própria
aflição, à aflição de outros, ou ambos? É uma ação sem habilidade, com
conseqüências dolorosas, resultados dolorosos?' Se você concluir que ela
conduzirá à aflição, não a pratique. Se não, vá em frente, pratique-a. <span
style="mso-spacerun: yes"> </span>Enquanto estiver agindo, pergunte a si mesmo
se existem conseqüências ruins inesperadas surgindo. Se existirem, pare. Se não
existirem, continue com o que você estava fazendo. Ao concluir a ação, analise
as suas conseqüências no curto e longo prazo. Se uma ação com a linguagem ou
com o corpo acabou produzindo aflição, informe um companheiro praticante
experiente (é por essa razão que o Buda estabeleceu a Sangha) e ouça o conselho
que essa pessoa lhe der. Se a ação equivocada for apenas um ato mental, tente
desenvolver o desgosto por esse tipo de pensamento. Em ambos os casos decida
nunca mais repetir o mesmo erro novamente e empregue a sua perspicácia para
fazer com que essa determinação seja lograda. Se, no entanto, as conseqüências
no longo prazo da ação original forem inócuas, sinta-se feliz e satisfeito por
estar no caminho correto e continue com o seu treinamento.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A
perseverança nessa linha de interrogação estimula dois resultados principais.
Para começar, você se torna mais sensível às suas ações e com mais respeito
pelas suas conseqüências, tanto no presente como ao longo do tempo. Ao
contrário da criança que diz, “Já estava quebrado quando eu pisei,” você tem
consciência quando quebra as coisas – físicas ou mentais – e quando não as
quebra. Ao mesmo tempo, você obtém maestria nos padrões de causa e efeito. Você
se torna cada vez melhor no lidar com as coisas sem que elas se quebrem. Isto
por outro lado estimula uma saudável noção de “eu” baseada na competência e
habilidade. A sua noção de eu se torna bem humorada o suficiente para
espontaneamente admitir erros, madura o suficiente para aprender com eles, ágil
o suficiente para notar os efeitos imediatos das suas ações, enquanto que
paciente o suficiente para se esforçar por metas a longo prazo. Confiante no
seu próprio poder de observação, esse “eu” também tem a humildade necessária
para aprender com a experiência e aconselhamento dos outros. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Esses dois
resultados – sensibilidade para com as conseqüências das suas próprias ações e
uma adequada noção de um eu – permitem que você se acomode num nível de
concentração mental que é sólida e nutritiva. Você elimina o obstáculo da
incerteza quanto ao que é hábil e inábil e é capaz de desenvolver as qualidades
hábeis necessárias para centrar a mente. A medida que esse foco centralizado se
desenvolve, uma coisa interessante ocorre: a sua sensibilidade com relação às
ações e a noção de um eu se encontram cara a cara. Você começa a ver aquele eu
não como uma coisa, mas como uma atividade, um processo de “fazer um eu” e
“fazer o meu” no qual você repetidamente cria e recria a noção de quem você é.
Você também começa a notar que fazer um eu, mesmo quando isso produz o mais
hábil eu possível, inevitavelmente resulta em sofrimento.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Porque?
Porque toda noção de “eu” ou “meu” envolve apego – mesmo se a sua concentração
estiver sintonizada com a noção de um eu universal – e todo apego produz
sofrimento. Portanto, para conduzir o desenvolvimento da habilidade até o seu
último grau, você tem que desaprender o hábito de fazer o eu e fazer o meu. E
para realizar isso, um outro conjunto de perguntas se faz necessário.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Essas são
as questões que introduzem a estratégia do não-eu. O Buda recomenda que você
foque em qualquer fenômeno à sua volta em relação ao qual você<span
style="mso-spacerun: yes">  </span>sinta como “eu” ou “meu” e faça uma série de
perguntas, começando com: “Isso é permanente ou impermanente?” Se você se
identifica com o seu corpo, olhe para ele. Verá que ele sente fome e sede, que
está envelhecendo, destinado a ficar enfermo e morrer. “E aquilo que é impermanente
é </span>sofrimento ou felicidade<span lang=PT-BR style='mso-ansi-language:
PT-BR'>?” Veja o esforço para encontrar a felicidade estável baseada no corpo e
você verá o quanto isso é estressante. “E é adequado considerar o que é
impermanente, sofrimento, sujeito a mudanças como: 'Isso é meu. Isso sou eu.
Isso é o meu eu’?” Responda o que você crê ser adequado.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Continue
nessa linha de questionamento em direção ao seu interior, através de camadas e
mais camadas de eventos físicos e mentais, até que você encontre o alto comando:
o eu que está gerenciando não só a estabilidade da sua concentração, mas também
o seu diálogo interno de perguntas e respostas. Fortificado com a sensação de
estabilidade e calma que surgem com a intensa concentração, você poderá começar
a desmontar aquele eu sem ansiedade em relação a o que irá ocorrer quando ele
desaparecer. E quando as intenções que compõem aquele eu são desmontadas, uma
coisa estranha acontece. É como se você puxasse o fio estratégico que mantém
unido todo um tapete e todo o conjunto se desmancha por si mesmo. Tudo aquilo
que possa estar pegado se solta. O que resta é a completa e absoluta liberdade
– livre do tempo e espaço, de ambos, eu e não-eu, pois ambos “eu” e “não-eu”
são percepções transcendidas por aquela liberdade.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Mesmo que
você tenha experimentado apenas um primeiro gosto sutil dessa liberdade, você
poderá apreciar a destreza com a qual o ensinamento sobre o não-eu responde à
questão “O que é hábil?” E você compreenderá porque o Buda recomenda deixar de
lado a questão “Quem sou eu?” Para começar, ela não o teria conduzido a esta
liberdade e poderia muito bem atrapalhar o caminho. Como o seu “eu” é um
processo, qualquer tentativa de fazer com que ele se fixe antes que você tenha
obtido a maestria sobre esse processo é igual a ficar golpeando sombras,
distraído da verdadeira tarefa a ser feita. Qualquer tentativa de desmontar o
seu “eu” antes que ele tenha se tornado saudável e maduro resultaria numa
libertação neurótica e insegura: você estaria apenas fugindo daquelas partes
desordenadas e mal cuidadas da sua vida. Além disso, qualquer resposta à
pergunta “Quem sou eu?” seria totalmente <a name=PVW>inapropriada</a> para
descrever a liberdade recém-descoberta, pois esta é uma outra dimensão, onde os
conceitos de “eu,” “não-eu,” “sou,” “não sou” não se aplicam.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A única
questão que ainda o preocuparia é como desenraizar as raízes inábeis ainda
latentes na mente. Uma vez que elas tenham sido desenraizadas, o Buda promete
que não haverá obstáculo para a realização plena e completa da libertação. E
com essa liberdade, a mente não carece de nada, não possui nada em excesso. Não
existe mais delusão ou cobiça, ou raiva que possam dar origem a questões
urgentes. As únicas questões que permanecem equivalem a um bônus: qual a melhor
forma de tomar as habilidades desenvolvidas ao longo do caminho e empregá-las
em benefício do mundo.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>E o que
mais haveria para ser perguntado?<o:p></o:p></span></p>


