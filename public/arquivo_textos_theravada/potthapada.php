<p class=Normal>Este sutta retrata os dois modos através dos quais o Buda respondia às questões controvérsas da sua época. O primeiro modo – ilustrado pela sua contribuição na discussão sobre a cessação última da percepção – era adotar os termos da discussão mas investi-los com os significados por ele atribuídos e depois tentar dirigir a discussão para a prática que conduz à cessação do sofrimento. O segundo modo – ilustrado através do tratamento dado à pergunta se o mundo é eterno, etc. – era de declarar que esse tipo de pergunta não conduz à iluminação e recusar-se a tomar uma posição com relação a ela. </p>

<p class=Normal>Vários outros suttas – tal como o <a href="../sutta/MN63.php">MN 63</a>, <a href="../sutta/MN72.php">MN 72</a> e <a href="../sutta/ANX.93.php">AN X.93</a> – retratam o Buda e os seus discípulos adotando o segundo modo. Este sutta é incomum no retrato prolongado do Buda adotando o primeiro. Muitos dos termos técnicos empregados aqui, como por exemplo: a verdadeira e refinada percepção, o pico da percepção, passos atentos sucessivos, a realização da cessação última da percepção, aquisições de um eu - não são encontrados em nenhum outro lugar do Cânone. Ao final do sutta ele os descreve como “apenas nomes, expressões, modos de linguagem, designações de uso corrente no mundo, que o Tathagata usa sem se agarrar nelas.” Em outras palavras, ele as toma tendo em vista o propósito imediato e depois as deixa de lado. Portanto elas não devem ser tomadas como parte do núcleo dos seus ensinamentos. Ao invés disso elas devem ser lidas como exemplos da sua habilidade em adaptar a linguagem dos seus interlocutores aos seus próprios objetivos. Por essa razão, este sutta é melhor ser lido depois que a pessoa tenha lido outros suttas e esteja mais familiarizada com os conceitos centrais dos ensinamentos do Buda. </p>

<p class=Normal>O que há de particularmente interessante neste sutta é o tratamento dado pelo Buda às três “aquisições de um eu.” O primeiro – o eu grosseiro – se refere à noção comum, normal, de identificar-se com o próprio corpo. As duas últimas – o eu feito pela mente e o eu sem forma – se referem à noção de um eu que pode ser desenvolvida com a meditação. O eu feito pela mente pode resultar de uma experiência do corpo feito pela mente – o “corpo astral” – que constitui um dos poderes que pode ser desenvolvido através da prática da concentração. O eu sem forma pode resultar de qualquer uma das realizações imateriais, os estados de concentração cujo objeto é sem forma, tal como a experiência do espaço infinito, da consciência infinita, do nada. Embora os meditadores, ao experimentarem esses estados possam assumir terem encontrado o seu “eu verdadeiro,” o Buda é cuidadoso ao observar que essas são aquisições, e que elas não são um eu mais verdadeiro do que o próprio corpo. Elas são uma aquisição do eu apenas durante o tempo em que a pessoa com elas se identifique. O Buda segue dizendo que ele ensina o Dhamma com o propósito de abandonar todas aquisições de um eu “de tal modo que ao praticá-lo, as qualidades mentais impuras sejam abandonadas e as qualidades mentais luminosas se fortaleçam, e você entre e permaneça na culminação e perfeição da sabedoria aqui e agora, tendo realizado e alcançado isso por você mesmo." </p>



