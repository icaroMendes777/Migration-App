<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Satipatthana Vipassana<o:p></o:p></span></b></p>

<p class=Normal align=center style='text-align:center'><i style='mso-bidi-font-style:
normal'><span lang=PT-BR style='mso-ansi-language:PT-BR'>Namo Buddhasa<br>

Homenagem ao Perfeitamente
Iluminado<o:p></o:p></span></i></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ao
encontrarmos o Ensinamento do Buda, é muito importante que cada um cultive as
qualidades da conduta virtuosa <i style='mso-bidi-font-style:normal'>(sila), </i>concentração
<i style='mso-bidi-font-style:normal'>(samadhi)</i> e sabedoria <i
style='mso-bidi-font-style:normal'>(pañña).</i> A pessoa deve sem dúvida
possuir essas três qualidades.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Para as pessoas leigas, o
comportamento virtuoso mínimo esperado é a observação dos Cinco Preceitos. Para
os bhikkhus é a observação do Patimokkha, o código de disciplina monástica.
Qualquer um que seja bem disciplinado no comportamento virtuoso irá renascer
num plano feliz de existência como um ser humano ou como um <i
style='mso-bidi-font-style:normal'>deva.<o:p></o:p></i></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>No entanto, essa forma
comum de virtude mundana <i style='mso-bidi-font-style:normal'>(lokiya-sila)</i>
não servirá como proteção contra a recaída nos planos inferiores de existência
miserável, tal como os infernos, o reino animal, ou o reino dos <i>petas</i>
(fantasmas). É, portanto, desejável cultivar a forma superior de virtude
supramundana <i style='mso-bidi-font-style:normal'>(lokuttara-sila). </i>Quando
alguém tiver adquirido a virtude supramundana, estará seguro de não recair nos
planos inferiores e irá ter sempre uma vida feliz ao renascer como ser humano
ou <i>deva</i>. Todos deveriam, portanto, tomar como tarefa o trabalho pela
virtude supramundana.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Existe uma real esperança
de sucesso para qualquer um que se dedique com sinceridade e seriedade. Seria
de fato uma pena se alguém não tomasse esta oportunidade para adquirir
qualidades mais elevadas, pois tal pessoa será sem dúvida, mais cedo ou mais tarde,
vítima do seu próprio <i>kamma</i> ruim que irá puxá-la para os níveis
inferiores de existência miserável nos infernos, no reino animal, ou na esfera
dos <i>petas</i>, onde a duração da vida se estende por centenas, milhares ou
milhões de anos. Fica, portanto, enfatizado que encontrar o Ensinamento do Buda
é uma oportunidade única para trabalhar pelo caminho da virtude<span
style="mso-spacerun: yes">  </span><i style='mso-bidi-font-style:normal'>(magga-sila)</i>
e pelo fruto da virtude <i style='mso-bidi-font-style:normal'>(phala-sila).</i><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Não é, no entanto,
aconselhável trabalhar apenas pela conduta virtuosa. É necessário também
praticar o <i style='mso-bidi-font-style:normal'>samadhi</i> ou concentração. <i>Samadhi</i>
é o estado concentrado e tranqüilo da mente. A mente comum ou não treinada
tem o hábito de perambular por vários lugares. Ela não pode ser mantida sob
controle pois, segue atrás de qualquer idéia, pensamento ou imaginação, etc.
Para evitar essa perambulação, a mente deve ser treinada para dar atenção
constante a um objeto de meditação escolhido. Desenvolvendo essa prática a
mente gradualmente abandona as distrações e permanece fixa no objeto para o
qual está direcionada. Isto é <i>samadhi</i>.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Existem dois tipos de
concentração: concentração mundana <i style='mso-bidi-font-style:normal'>(lokiya-samadhi)
</i>e concentração supramundana <i style='mso-bidi-font-style:normal'>(lokuttara-samadhi).</i>
Dessas duas, a primeira consiste de absorções mundanas, tais como as quatro <i
style='mso-bidi-font-style:normal'>rupa-jhanas </i>– as absorções correspondentes
ao reino da matéria sutil – e as quatro <i style='mso-bidi-font-style:normal'>arupa-jhanas
</i>– as absorções correspondentes ao reino imaterial. Essas absorções podem
ser alcançadas através da prática da meditação da tranqüilidade <i
style='mso-bidi-font-style:normal'>(samatha-bhavana)</i> com métodos tais como
a atenção plena na respiração, amor bondade <i style='mso-bidi-font-style:normal'>(metta),
</i>kasinas, etc. Como resultado dessas realizações ocorrerá o renascimento no
plano dos <i>brahmas</i>. O tempo de vida de um <i>brahma</i> é bastante longo
e pode durar um ciclo cósmico, dois, quatro, oito até um limite de 84.000
ciclos cósmicos, conforme for o caso. Mas, ao fim desse tempo de vida um <i>brahma</i>
irá morrer e renascer como um ser humano ou um <i>deva</i>.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Se alguém leva uma vida
virtuosa o tempo todo, poderá desfrutar de uma vida feliz num plano de
existência superior, mas, como não estará livre das impurezas da cobiça,
aversão e delusão, poderá cometer atos demeritórios em muitas ocasiões. A
pessoa será então vítima do seu próprio <i>kamma</i> ruim e irá renascer no
inferno ou em outros planos inferiores de existência miserável. Portanto, a concentração
mundana também não oferece segurança definitiva. É aconselhável trabalhar pela
concentração supramundana, a concentração dos caminhos supramundanos <i
style='mso-bidi-font-style:normal'>(magga) </i>e dos seus frutos <i
style='mso-bidi-font-style:normal'>(phala).</i> Para adquirir essa concentração
é essencial cultivar a sabedoria <i style='mso-bidi-font-style:normal'>(pañña).</i><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Existem dois tipos de
sabedoria: mundana e supramundana. Hoje em dia, o conhecimento de literatura,
arte, ciência ou outros assuntos mundanos é em geral considerado como uma forma
de sabedoria, porém, essa forma de sabedoria não tem nada a ver com o
desenvolvimento mental <i style='mso-bidi-font-style:normal'>(bhavana).</i> Nem
pode ser vista como proporcionando mérito real pois muitas armas de destruição
são inventadas por meio desse tipo de conhecimento que sempre está sob a
influência da cobiça, aversão e outras motivações inábeis. O verdadeiro
espírito da sabedoria mundana, por outro lado, só possui méritos e nenhum
demérito de qualquer tipo<i style='mso-bidi-font-style:normal'>.</i> A
verdadeira sabedoria mundana inclui: o conhecimento empregado nas atividades de
bem-estar social e auxílio que não causam dano; aprender a obter o conhecimento
do verdadeiro significado ou sentido das escrituras (budistas); e as três
classes de conhecimento para o insight <i style='mso-bidi-font-style:normal'>(vipassana-bhavana),
</i><span style='mso-bidi-font-style:italic'>que são</span> o conhecimento
proveniente do aprendizado <i style='mso-bidi-font-style:normal'>(sutamaya-pañña),
</i>o conhecimento proveniente da reflexão <i style='mso-bidi-font-style:normal'>(cintamaya-pañña)
</i>e o conhecimento proveniente do desenvolvimento meditativo <i
style='mso-bidi-font-style:normal'>(bhavanamaya-pañña).</i> A virtude de
possuir a sabedoria mundana irá conduzir a uma vida feliz num plano elevado de
existência, mas, ainda não poderá prevenir o risco de renascer no inferno ou em
outros estados de existência miserável. Só o desenvolvimento da sabedoria
supramundana <i style='mso-bidi-font-style:normal'>(lokuttara-pañña) </i>poderá
definitivamente remover esse risco.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A sabedoria supramundana é
a sabedoria dos caminhos supramundanos e dos seus frutos. Para desenvolver essa
sabedoria é necessário praticar a meditação de insight <i style='mso-bidi-font-style:
normal'>(vipassana-bhavana)</i> tendo como base as três disciplinas da virtude,
concentração e sabedoria. Ao desenvolver de forma adequada a qualidade da
sabedoria, as qualidades da virtude e concentração também serão adquiridas.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>O Desenvolvimento da
Sabedoria<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O método para desenvolver
essa sabedoria é observar a materialidade <i style='mso-bidi-font-style:normal'>(rupa)
</i>e a mentalidade <i style='mso-bidi-font-style:normal'>(nama) – </i>os
únicos dois elementos que existem num ser vivo – com o objetivo de entender a
sua verdadeira natureza. Na atualidade, os experimentos para a observação
analítica da materialidade são normalmente executados em laboratórios com o
auxilio de vários instrumentos, no entanto, esses métodos não são capazes de
lidar com a mente. O método do Buda não requer nenhum tipo de instrumento ou
ajuda externa. Ele pode lidar com ambos, a materialidade e a mentalidade com
bastante êxito. A própria mente é utilizada para efeitos de análise através da
fixação da atenção plena sobre as atividades da materialidade e mentalidade à
medida que elas ocorrem na própria pessoa. Ao repetir continuamente este tipo
de exercício, a concentração necessária poderá ser alcançada, e quando ela for
suficientemente apurada, o fluxo contínuo de origem e cessação da mentalidade e
materialidade será percebido de modo vívido.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Um ser vivo consiste apenas
dos dois grupos distintos de materialidade e mentalidade. A substância sólida
do corpo, tal como é encontrada, pertence ao grupo da materialidade. De acordo
com a enumeração usual dos fenômenos materiais, existem no total vinte oito
tipos de fenômenos materiais no grupo da materialidade, mas de forma
simplificada pode ser observado que o corpo é uma massa de materialidade. Por
exemplo, é o mesmo que uma boneca feita de argila ou trigo, que nada mais é do
que uma coleção de partículas de argila ou trigo. A materialidade muda a sua
forma <i style='mso-bidi-font-style:normal'>(ruppati) </i>sob as condições
físicas de calor, frio, etc., e devido a essa mutabilidade sob condições
físicas diferentes, é denominada <i style='mso-bidi-font-style:normal'>rupa </i>em
Pali. A materialidade não possui nenhuma faculdade para conhecer um objeto.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>No Abhidhamma, os elementos
de mentalidade e materialidade são classificados respectivamente como “estados
com objeto” <i style='mso-bidi-font-style:normal'>(sarammana-dhamma) </i>e
“estados sem objeto” <i style='mso-bidi-font-style:normal'>(anarammana-dhamma).
</i>O elemento de mentalidade possui um objeto, detém um objeto, conhece um
objeto, enquanto que aquele da materialidade não possui um objeto, não detém um
objeto e não conhece um objeto. Assim, pode-se ver que o Abhidhamma afirma de
forma direta que a materialidade não possui a faculdade de conhecer um objeto.
Um meditador também percebe desta maneira, que a “materialidade não possui a
faculdade de conhecimento.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Lenha e colunas, tijolos,
pedras e torrões de terra são massas de materialidade. Eles não possuem a
faculdade de conhecimento. O mesmo se dá com a materialidade que constitui um
corpo vivo - não possui a faculdade de conhecimento. As pessoas, no entanto,
possuem em comum a idéia de que a materialidade de um corpo vivo possui a
faculdade de conhecer um objeto e de que perde essa faculdade somente com a
morte. Mas, na verdade não é assim. Na verdade, a materialidade, quer seja num
corpo vivo ou num cadáver, não possui a faculdade de conhecer um objeto.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O que é então aquilo que
conhece um objeto? É a mentalidade, que surge na dependência da materialidade.
É chamada <i style='mso-bidi-font-style:normal'>nama </i>em Pali porque se
inclina <i style='mso-bidi-font-style:normal'>(namati)</i> em direção a um
objeto. A mentalidade também é referida como pensamento ou consciência. A
mentalidade surge na dependência da materialidade: na dependência do olho, a
consciência no olho surge (visão); na dependência do ouvido, a consciência no
ouvido surge (audição); na dependência do nariz, a consciência no nariz surge
(olfato); na dependência da língua, a consciência na língua surge (sabor); na
dependência do corpo, a consciência no corpo surge (tangível</span><span
lang=PT-BR style='font-size:11.0pt;mso-bidi-font-size:12.0pt;mso-ansi-language:
PT-BR'>). </span><span lang=PT-BR style='mso-ansi-language:PT-BR'>Existem
muitos tipos de tangíveis, bons e maus.</span><span lang=PT-BR
style='font-size:11.0pt;mso-bidi-font-size:12.0pt;mso-ansi-language:PT-BR'><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Enquanto os tangíveis
possuem um amplo campo de ação ao percorrer toda a extensão do corpo,
internamente e externamente, o sentido da visão, audição, olfato e paladar
surgem na sua esfera particular – o olho, o ouvido, o nariz e a língua – cada
um dos quais ocupando uma área pequena e delimitada no corpo. Esses sentidos do
tato, visão, etc., não são nada mais que elementos da mente. Na mente também
ocorre a consciência<span style="mso-spacerun: yes">  </span>– pensamentos,
idéias, fantasias, etc. – que depende da base da mente. Todos estes são
elementos da mente. A mente conhece um objeto, enquanto que a materialidade não
conhece um objeto.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'><span style="mso-spacerun:
yes"> </span>Visão<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em geral as pessoas
acreditam que, no caso da visão, é o olho que na verdade vê. Elas pensam que a
visão e o olho são a mesma coisa. Elas também pensam que: “A visão sou eu,” “Eu
vejo as coisas,” “O olho, a visão e eu somos uma única pessoa.” Não é assim, na
realidade. O olho é uma coisa e a visão é outra e não existe uma entidade
separada tal como o “eu” ou “ego.” Existe apenas a realidade da visão que surge
na dependência do olho.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Para dar um exemplo, é como
o caso de uma pessoa que está dentro de uma casa. A casa e a pessoa são duas
coisas distintas: a casa não é a pessoa, nem a pessoa é a casa. Da mesma forma
com a visão. O olho e a visão são duas coisas distintas: o olho não é a visão,
nem a visão é o olho.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Para dar um outro exemplo,
é como o caso de uma pessoa num quarto, que ao abrir a janela vê muitas coisas.
Se ela for perguntada, “Quem é que vê? É a janela ou a pessoa que na verdade
vê?” a resposta é, “A janela não possui a habilidade de ver; é apenas a pessoa
que vê.” Se perguntada outra vez, “A pessoa será capaz de ver as coisas no
exterior sem a janela?” a resposta será, “Não é possível enxergar através da
parede sem uma janela. Só é possível ver através da janela.” Da mesma forma, no
caso da visão existem duas realidades separadas, o olho e a visão. O olho não é
a visão, nem a visão é o olho, porém, não pode existir um ato de ver sem o
olho. Na realidade, a visão surge na dependência do olho.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Agora está evidente que no
corpo existem apenas dois elementos distintos, de materialidade (olho) e
mentalidade (visão) em cada momento de ver. Além disso, existe também um terceiro
elemento de materialidade – o objeto visual. Em certas ocasiões o objeto visual
é notado dentro do corpo e outras vezes é notado fora do corpo. Com a adição do
objeto visual serão então três elementos, dois dos quais (o olho e o objeto
visual) são a materialidade e o terceiro (visão) a mentalidade. O olho e o
objeto visual, sendo materialidade, não possuem a faculdade de conhecer<span
style="mso-spacerun: yes">  </span>um objeto, enquanto que a visão, sendo
mentalidade, pode conhecer o objeto visual e determinar o que ele é. Agora está
claro que existem apenas os dois elementos separados de materialidade e
mentalidade no momento de ver e o surgimento desse par de elementos separados é
conhecido como visão.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>As pessoas que não possuem
o treinamento e o conhecimento da meditação de insight têm a idéia de que a
visão pertence ou é o “eu”, “ego,” “uma entidade viva,” ou “uma pessoa.” Elas
acreditam que “a visão sou eu,” ou “eu sou a visão,” ou “eu sou o
conhecimento.” Esse tipo de idéia ou entendimento é chamado <i>sakkaya-ditthi </i>em
Pali. <i>Sakkaya</i> significa o grupo de materialidade <i>(rupa) </i>e
mentalidade <i>(nama) </i>com<span style='mso-bidi-font-style:italic'> suas </span>existências
distintas. <i>Ditthi </i>significa o entendimento, idéia ou crença incorreta. A
palavra composta <i>sakkaya-ditthi </i>significa o entendimento incorreto ou
crença num eu em relação a <i>nama </i>e <i>rupa.<o:p></o:p></i></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Para maior clareza,
explicaremos um pouco mais sobre como o entendimento ou idéia incorreta é
sustentada. No momento de ver, as coisas que na realidade existem são o olho, o
objeto visual (ambos materialidade) e a visão (mentalidade). <i>Nama </i>e <i>rupa
</i>são a realidade, no entanto, as pessoas possuem a idéia de que esse grupo
de elementos é o eu, ou ego, ou uma entidade viva. Elas consideram que “ver é o
eu,” ou “aquilo que vê é o eu,” ou “eu vejo meu próprio corpo.” Portanto, esse
entendimento incorreto toma o simples ato de ver como sendo o eu, que é <i>sakkaya-ditthi,</i>
o entendimento incorreto de um eu.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Enquanto a pessoa não se
libertar do entendimento incorreto de um eu, não poderá, com segurança, escapar
do risco de cair num dos planos miseráveis dos infernos, animais ou petas.
Embora possa viver uma vida feliz no mundo humano ou dos <i>devas</i> por
virtude dos seus méritos, ainda assim estará sujeita, em qualquer momento, a
cair num dos estados de existência miserável quando os deméritos frutificarem.
Por essa razão, o Buda apontou ser essencial trabalhar pela completa
erradicação do entendimento incorreto de um eu:<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Que um bhikkhu se empenhe com a atenção plena para abandonar a idéia de
um eu” <i>(sakkaya-ditthippahanaya sato bhikkhu paribbaje)<o:p></o:p></i></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Explicando: embora seja o
desejo de todos evitar o envelhecimento, enfermidade e morte, ninguém pode
impedir o seu inevitável re-surgimento. Após a morte, segue o renascimento. O
renascimento em algum estado de existência não depende do desejo de cada um.
Não é possível evitar o renascimento nos infernos, no reino dos animais ou no
reino dos <i>petas</i> apenas através do desejo. O renascimento pode ocorrer em
qualquer estado de existência como conseqüência das próprias ações: não existe
outra opção. Por essa razão, o ciclo de nascimentos e morte, <i>samsara,</i> é
tão terrível. Portanto, todo esforço deve ser feito para que cada um se
familiarize com as condições miseráveis do <i>samsara, </i><span
style='mso-bidi-font-style:italic'>e então venha a t</span>rabalhar para
escapar do <i>samsara</i> para alcançar <i>Nibbana</i>.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Se escapar do <i>samsara</i>
de vez<span style="mso-spacerun: yes">  </span>não é possível no momento, um
esforço deve ser feito para pelo menos escapar do ciclo de renascimentos nos
infernos, no reino animal e no reino dos <i>petas</i>. Neste caso é necessário
trabalhar pela completa remoção de <i>sakkaya-ditthi, </i>que é a causa
primária do renascimento nos estados miseráveis de existência. <i>Sakkaya-ditthi
</i>só pode ser completamente destruído através do nobre caminho e do seu
fruto: as três qualidades supramundanas da virtude, concentração e sabedoria.
É, portanto, imperativo trabalhar pelo desenvolvimento dessas qualidades. Como
se deve trabalhar? Através da notação ou observação a pessoa deve sair da
esfera de influência das contaminações <i>(kilesa). </i>Ela deve praticar
notando ou observando constantemente cada ato de ver, ouvir, etc., que são os
processos físicos e mentais, até que se liberte de <i>sakkaya-ditthi, </i>o
entendimento incorreto do eu.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Por essas razões, o
conselho para adotar a prática da meditação <i>vipassana</i><span
style='mso-bidi-font-style:italic'> é invariavelmente dado aqui</span>. Agora,
os meditadores que vieram a este Centro com o propósito de praticar a meditação
<i>vipassana</i><span style="mso-spacerun: yes">  </span>deverão ser capazes de
completar o curso de treinamento e alcançar o nobre caminho em pouco tempo. O
entendimento do eu será então completamente removido e a proteção contra o
perigo de renascer nos reinos dos infernos, animais e <i>petas</i> será obtida.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Com respeito a isso, o
exercício é simplesmente notar ou observar os elementos existentes em cada ato
de ver. Deve ser notado como “vendo, vendo” em cada ocasião que se vê. Os
termos “notar” ou “observar” ou “contemplar” significam<span
style="mso-spacerun: yes">  </span>o ato de manter a mente fixada no objeto com
o objetivo de conhecê-lo de forma clara.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isso é feito, e o
ato de ver é notado como “vendo, vendo,” em certas ocasiões o objeto visual é
notado, em outras a consciência de ver é notada, em outras a base do olho, o
lugar do qual se vê é notado. Será suficiente se a pessoa puder notar com
clareza qualquer um dos três. Se não, baseado neste ato de ver irá surgir <i>sakkaya-ditthi,</i>
que irá enxergar sob a forma de uma pessoa ou como pertencendo a uma pessoa, ou
como sendo permanente, prazeroso e como eu. Isso irá estimular as contaminações
do desejo e do apego que por seu lado irão gerar ações e as ações irão resultar
num renascimento, numa nova existência. Assim opera o processo de origem
dependente e o círculo vicioso do <i>samsara</i> gira sem cessar. Para evitar o
círculo do <i>samsara</i> a partir desse ato de ver, é necessário notar “vendo,
vendo” em cada ocasião que estiver vendo.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Audição, Etc.<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Da mesma forma, no caso da audição
existem apenas dois elementos distintos, materialidade e mentalidade. O sentido
da audição surge na dependência do ouvido. Enquanto que o ouvido e o som são
dois elementos da materialidade, o sentido da audição é um elemento da
mentalidade. Para conhecer de forma clara qualquer um desses dois elementos de
mentalidade e materialidade, em cada ocasião que estiver<span
style="mso-spacerun: yes">  </span>ouvindo deve ser notado como “ouvindo,
ouvindo.” Assim também, “cheirando, cheirando” deve ser notado em cada ocasião
que estiver<span style="mso-spacerun: yes">  </span>cheirando, e “saboreando,
saboreando” em cada ocasião que estiver saboreando.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O tangível no corpo deve
ser notado da mesma forma. Existe um tipo de elemento material em todo o corpo
conhecido como sensibilidade corporal, que recebe todas as sensações tácteis.
Todas as sensações tácteis, quer sejam agradáveis ou desagradáveis, em geral
entram em contato com a sensibilidade corporal e a partir disso surge a
consciência no corpo que sente ou conhece a sensação táctil de cada evento.
Pode ser visto que a cada sensação táctil existem dois elementos de
materialidade – a sensibilidade corporal e o objeto tangível – e um elemento de
mentalidade – conhecendo a sensação táctil.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Para conhecer essas coisas
de forma clara, a cada sensação táctil, a prática de notar como “toque, toque”
deve ser feita. Isto se refere apenas à forma comum da sensação táctil. Existem
formas especiais que acompanham as sensações dolorosas ou desagradáveis, tal
como sentir tensão ou cansaço no corpo ou membros, sentir calor, dor,
dormência, incômodo, etc. Como a sensação <i>(vedana) </i>predomina nesses
casos, deve ser notado como “sensação de calor,” “sensação de cansaço,”
“sensação de dor,” etc., de acordo com o caso.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Também deve ser mencionado
que ocorrem muitas sensações nas mãos, pernas, etc., em cada ação de dobrar,
esticar ou mover. Pelo fato da mentalidade querer mover, esticar ou dobrar, as
atividades materiais de mover, esticar ou dobrar, etc. ocorrem em sucessão.
(Pode ser que no início essas ocorrências não sejam notadas. Elas poderão ser
notadas apenas depois de algum tempo ao ganhar-se experiência com a prática.
Isto é mencionado aqui para efeito de informação geral.) Todas as atividades de
movimento e mudanças, etc., são feitas pela mentalidade. Quando a mentalidade
quer dobrar, surge uma série de movimentos internos da mão ou perna. Quando a
mentalidade quer esticar ou mover, surgem uma série de movimentos externos ou
movimentos de ir e vir. Eles desaparecem imediatamente depois que ocorreram, no
ponto exato da ocorrência, como poderá ser notado com mais experiência.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em cada evento de dobrar,
esticar, ou outras atividades, surge primeiro uma série de intenções, momentos
de mentalidade, induzindo ou causando nas mãos e pernas uma série de atividades
materiais, tais como enrijecer, dobrar, esticar ou mover para cá e para lá.
Essas atividades vão de encontro a outros elementos materiais, a sensibilidade
corporal, e em cada evento de contato entre as atividades materiais e as
qualidades de sensação surge a consciência no corpo que sente ou conhece a sensação
táctil. Fica, portanto, claro que as atividades materiais são fatores
predominantes nesses casos. É necessário notar esses fatores predominantes. Se
não, com certeza irá surgir o entendimento incorreto que considera essas
atividades como ações de um “eu” – “eu estou dobrando,” “eu estou esticando,”
“minhas mãos,” ou “minhas pernas.” A prática de notar como “dobrando,”
“esticando,” “movendo,” é feita com o propósito de remover esse entendimento
incorreto.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Mente<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Na dependência da base da
mente surge uma série de atividades mentais, tais como pensar, imaginar, etc.,
ou falando de uma forma geral, uma série de atividades mentais surgem na
dependência do corpo. Na realidade, cada caso é uma composição de mentalidade e
materialidade,<span style="mso-spacerun: yes">  </span>a base da mente sendo a
materialidade e o pensamento, imaginação e assim por diante, sendo a
mentalidade. Para ser capaz de notar a materialidade e a mentalidade com
clareza, “pensando,” “imaginando” e assim por diante, deve ser notado em cada
caso.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Depois de haver executado a
prática da maneira indicada por algum tempo, poderá ocorrer uma melhoria na
concentração. A pessoa irá notar que a mente não mais perambula por vários
lugares, mas permanece fixa no objeto para o qual está direcionada. Ao mesmo
tempo, o poder de observação terá se desenvolvido de forma considerável. Em
cada ocasião em que uma notação é feita, apenas dois processos de materialidade
e mentalidade são observados pela pessoa, um conjunto composto de um objeto
(materialidade) e um estado mental (mentalidade), surgindo ao mesmo tempo.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Prosseguindo com a prática
de meditação, depois de algum tempo será notado que nada é permanente, mas que
tudo se encontra num estado de movimento. Novas coisas surgem a cada instante.
Cada uma delas é notada ao surgir. Tudo que surge desaparece imediatamente e
imediatamente outra coisa surge, a qual é novamente notada e depois desaparece.
Assim o processo de surgimento e desaparecimento continua, o que mostra com
clareza que nada é permanente. A pessoa, dessa forma, compreende que “as coisas
não são permanentes” porque pode ver que elas surgem e desaparecem
imediatamente. Esse é o insight da impermanência <i>(aniccanupassana-ñana).</i><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então a pessoa compreende
que “o surgimento e desaparecimento não são desejáveis.” Esse é o insight do
sofrimento <i>(dukkhanupassana-ñana). </i>Ela em geral também experimenta
muitas sensações dolorosas no corpo, tais como cansaço, calor, dores, e ao
notar essas sensações a pessoa se dá conta de que este corpo é uma coleção de
sofrimentos. Isso também é o insight do sofrimento.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, a cada momento em
que uma notação é feita se observa que os elementos da materialidade e
mentalidade ocorrem de acordo com a sua respectiva natureza e condicionamento e
não de acordo com o desejo da pessoa. Assim ela compreende que “eles são
elementos; não podem ser governados; não são uma pessoa ou uma entidade viva.”
Esse é o insight do não-eu <i>(anattanupassana-ñana).<o:p></o:p></i></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ao adquirir de forma plena
esses insights da impermanência, sofrimento e não-eu, ocorre o amadurecimento
do conhecimento dos caminhos supramundanos <i>(magga-ñana) </i>e o conhecimento
dos seus frutos <i>(phala-ñana),</i> e Nibbana é realizado. Ao realizar Nibbana
no primeiro estágio, a pessoa estará livre do ciclo de renascimentos nos reinos
de existência miserável. Todos, portanto, deveriam se empenhar para realizar
esse primeiro estágio, o caminho e fruto de entrar na correnteza, como uma
medida de proteção mínima contra um renascimento desfavorável.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>O Exercício para o
Principiante<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Já foi explicado que o
método de prática na meditação vipassana é notar, ou observar, ou contemplar as
ocorrências sucessivas de ver, ouvir e assim por diante nas seis portas dos
meios dos sentidos. No entanto, não é possível que um principiante siga todos
esses incidentes sucessivos à medida que eles ocorrerem porque a sua atenção
plena <i>(sati), </i>concentração <i>(samadhi) </i>e compreensão <i>(ñana)</i>
ainda são muito débeis. Os momentos de ver, ouvir, cheirar, saborear, tocar e
pensar ocorrem com muita rapidez. Parece que ver ocorre ao mesmo tempo que
ouvir, que ouvir ocorre ao mesmo tempo que ver, que ver e ouvir ocorrem
simultaneamente, que ver, ouvir, pensar e imaginar sempre ocorrem
simultaneamente. Como eles ocorrem com tal rapidez, não é possível distinguir
qual ocorre primeiro e qual vem em segundo lugar.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Na realidade, ver não
ocorre ao mesmo tempo que ouvir, nem ouvir ocorre ao mesmo tempo que ver. Esses
incidentes só podem ocorrer um de cada vez. Um meditador que apenas tenha
começado a prática e que não tenha desenvolvido de forma adequada a sua atenção
plena, concentração e sabedoria não estará ainda em condições de observar todos
esses momentos individualmente, pois eles ocorrem em sucessão. Um principiante,
portanto, não precisa notar muitas coisas. Ele deve começar com bem poucas coisas.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ver e ouvir apenas ocorrem
quando se dá atenção aos seus objetos. Se a pessoa não prestar atenção a
nenhuma visão ou som, poderá passar o tempo sem que nenhum evento de ver ou
ouvir ocorra. Cheiros raramente ocorrem. A experiência de saborear ocorre somente
ao comer. No caso de ver, ouvir, cheirar e saborear, o meditador poderá
notá-los quando eles ocorrerem. As impressões corporais, no entanto, estão
sempre presentes. Elas ocorrem de forma clara o tempo todo. Enquanto a pessoa
estiver sentada, as impressões corporais de tensão ou a sensação de rigidez da
posição são sentidas com clareza. A atenção deve, portanto, estar fixada na
posição sentada e a notação “sentada, sentada, sentada” deve ser feita.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Sentar<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Sentar é uma postura ereta
do corpo que consiste de uma série de atividades físicas que são induzidas pela
consciência e que se compõem de uma série de atividades mentais. É igual a uma
bola de borracha inflada que mantém a sua forma arredondada através da
resistência do ar dentro dela. A postura de sentar é semelhante, pois o corpo é
mantido numa posição ereta através do processo contínuo das atividades físicas.
Uma boa dose de energia é necessária para erguer e manter em posição ereta uma
carga tão pesada como este corpo. As pessoas em geral assumem que o corpo é
levantado e mantido numa posição ereta através dos músculos. Essa premissa é de
certa forma correta pois os músculos, sangue, tendões e ossos não são nada mais
que materialidade. O elemento de tensão que mantém o corpo numa posição ereta
pertence ao grupo da materialidade e surge nos músculos, tendões, sangue, etc.,
por todo o corpo, da mesma forma como o ar na bola de borracha.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O elemento de tensão é o
elemento ar, conhecido como <i>vayo-dhatu.</i> O corpo é mantido numa posição
ereta através do elemento ar sob a forma de tensão que se renova continuamente.
Durante o sono ou sonolência, a pessoa se deita porque o suprimento de novos
materiais sob a forma de tensão é interrompido. O estado mental quando há muita
sonolência ou durante o sono é chamado <i>bhavanga, </i>o “contínuo da vida” ou
fluxo passivo subconsciente. Durante <i>bhavanga</i>, as atividades mentais
estão ausentes e por essa razão o corpo permanece deitado durante o sono ou
muita sonolência.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Durante o período desperto,
as atividades mentais intensas e alertas estão continuamente surgindo, e devido
a isso, o elemento ar surge na seqüência sob a forma de tensão. Para conhecer
esses fatos, é essencial notar a postura corporal atentamente como “sentada,
sentada, sentada.” Isso não quer dizer necessariamente que essa impressão
corporal de tensão deva ser buscada e notada. A atenção deve ser fixada apenas
no conjunto completo da postura sentada, isto é, o tronco inferior numa forma
circular dobrada e o tronco superior mantido ereto.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Pode ser que o exercício de
observar a mera posição sentada seja considerado fácil demais e que não
requeira muito esforço. Se for assim, há pouca energia <i>(viriya) </i>e
concentração <i>(samadhi) </i>em excesso. A pessoa irá em geral sentir preguiça
e não irá querer prosseguir repetindo por um longo tempo a notação de “sentada,
sentada, sentada.” A preguiça em geral ocorre quando existe um excesso de
concentração e falta de energia. Nada além de um estado de preguiça e torpor <i>(thina-middha).</i><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Mais energia deve ser
desenvolvida e para que isso ocorra o número de objetos notados deve ser
aumentado. Depois de notar “sentada,” a atenção deve ser dirigida para um ponto
no corpo onde a sensação táctil é sentida e uma notação “toque” deve ser feita.
Qualquer ponto na perna, mão ou quadril onde uma sensação táctil seja sentida
de forma clara irá servir. Por exemplo, depois de notar a posição do corpo
sentada como “sentada,” o ponto em que a sensação táctil é sentida deve ser
notado como “toque.” A notação deverá, portanto, ser repetida usando esses dois
objetos da <i>posição sentada </i>e <i>do ponto do toque</i> alternadamente,
como “sentada, toque, sentada, toque, sentada, toque.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os termos “notação,”
“notar,” “observar” e “contemplar” são aqui usados para indicar a fixação da
atenção num objeto. O exercício consiste em simplesmente notar ou observar, ou
contemplar o “sentar, tocar.” Quem já possui alguma experiência na prática de
meditação poderá achar que este é um exercício fácil, mas aqueles que não
possuem experiência prévia poderão achar que é um exercício um tanto quanto
difícil.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Expansão - Contração<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Um exercício mais simples e
fácil para um principiante é o seguinte: a cada respiração ocorre no abdômen um
movimento de expansão e contração. Um principiante deveria começar com o
exercício de notar esse movimento. Esse movimento de expansão e contração é
fácil de observar por sua característica mais tosca e portanto mais adequado
para quem está principiando. Como nas escolas onde as lições mais simples são
mais fáceis de aprender, assim também ocorre na prática da meditação <i>vipassana</i>.
Será mais fácil para um principiante desenvolver a concentração e o
conhecimento através de um exercício simples e fácil.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Novamente, o propósito da
meditação vipassana é de iniciar o exercício através da contemplação dos
fatores proeminentes do corpo. Dos dois fatores de mentalidade e materialidade,
o primeiro é sutil e menos proeminente, enquanto que o último possui uma
característica mais tosca e é mais proeminente. No princípio, portanto, o
procedimento mais comum para quem pratica a meditação de insight é de começar o
exercício contemplando os elementos materiais.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Com relação à
materialidade, pode ser mencionado aqui que a materialidade derivada <i
style='mso-bidi-font-style:normal'>(upada-rupa)</i> é sutil e menos proeminente,
enquanto que os quatro elementos físicos primários <i style='mso-bidi-font-style:
normal'>(maha-bhuta-rupa) </i>– terra, água, fogo e ar – são mais toscos e mais
proeminentes. Esses últimos portanto deveriam ter a prioridade na seqüência de
objetos para contemplação. No caso da expansão – contração, o fator que mais se
destaca é o elemento ar, ou <i style='mso-bidi-font-style:normal'>vayo-dhatu.</i>
O processo de enrijecimento e os movimentos do abdômen que são notados durante
a contemplação não são nada mais que as funções do elemento ar. Dessa forma,
será visto que o elemento ar é perceptível no início.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>De acordo com as instruções
do <i style='mso-bidi-font-style:normal'><a href="../sutta/MN10.php">Satipatthana
Sutta</a>, </i>a pessoa deve estar plenamente atenta às atividades de caminhar
enquanto caminha, àquelas de ficar em pé, sentar e deitar enquanto estiver em
pé, sentada e deitada respectivamente. A pessoa também deve estar atenta a
outras atividades corporais à medida que cada uma delas ocorra. A respeito
disto, é mencionado nos comentários que a pessoa deveria estar plenamente
atenta principalmente ao elemento ar, em preferência aos outros três elementos.
Na verdade, todos os quatro elementos primários são dominantes em cada ação do
corpo e é essencial perceber cada um deles. Ao iniciar a meditação, um dos dois
movimentos de expansão ou contração será notado de forma mais clara com cada
respiração e o princípio deve ser a notação desses movimentos.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Algumas das características
fundamentais do sistema de meditação vipassana foram explicadas para efeito de informação
geral. Iremos lidar agora com o esboço dos exercícios básicos.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Esboço dos exercícios
básicos<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ao contemplar a expansão e
contração, o discípulo deverá manter sua mente no abdômen. Assim ele irá
compreender o movimento para cima ou de expansão do abdômen ao inspirar e o
movimento para baixo ou contração ao expirar. Uma notação mental deve ser feita
como “expansão” para o movimento para cima e “contração” para o movimento para
baixo. Se esses movimentos não forem notados com clareza apenas com a fixação
da mente sobre eles, então uma ou as duas mãos devem ser colocadas sobre o
abdômen.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O meditador não deve tentar
modificar a sua maneira natural de respirar. Ele não deve tentar reduzir a
respiração através da retenção do ar, nem acelerar a respiração ou respirar
fundo. Se o fluxo natural da respiração for modificado surgirá logo o cansaço.
Portanto, ele deve manter o fluxo natural da respiração e dar prosseguimento à
contemplação da expansão e contração.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ao ocorrer o movimento
ascendente do abdômen, a notação mental “expansão” deve ser feita e no
movimento descendente do abdômen, a notação mental “contração” deve ser feita.
A notação mental desses termos não deve ser vocalizada. Na meditação <i>vipassana</i>
é mais importante conhecer o objeto do que conhecê-lo através de um nome ou
termo. É portanto necessário que o discípulo faça todo o esforço para estar
plenamente atento ao movimento de expansão desde o seu início até o seu final e
da contração desde o seu início até o seu final, como se esses movimentos estivessem
na verdade sendo vistos com os olhos. Assim que ocorrer a expansão, a mente que
toma conhecimento disso deve estar próxima do movimento, como no caso de uma
pedra atingindo uma parede. O movimento de expansão, à medida que ele ocorre, e
a mente que toma conhecimento disso têm que se encontrar a cada momento. Da
mesma forma, o movimento de contração, à medida que ele ocorre, e a mente que
toma conhecimento disso, têm que se encontrar a cada momento.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando não existir nenhum
outro objeto que seja evidente, o meditador deve dar continuidade ao exercício
de notar esses dois movimentos como “expansão, contração, expansão, contração,
expansão, contração.” Enquanto estiver ocupado com esse exercício, poderá haver
ocasiões em que a mente se desviará. Quando a concentração é fraca é muito
difícil controlar a mente. Embora esta seja dirigida para os movimentos de
expansão e contração, a mente não permanecerá com eles e irá se desviar para
outras coisas. Essa mente que se desvia para outras coisas não deve ser deixada
em paz. Deve ser notada como “desvio, desvio, desvio” assim que for notado que
ela se desviou. Ao notar uma ou duas vezes, a mente em geral pára de se desviar
e em seguida o exercício de notar a “expansão, contração” deve ser retomado.
Quando novamente for observado que a mente chegou a algum lugar, deve ser
notado como “chegando, chegando, chegando.” O exercício de notar a “expansão,
contração” deve ser retomado assim que os movimentos da mente estiverem claros.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ao encontrar com uma pessoa
na imaginação, deve ser notado como “encontro, encontro,” depois do que, o
exercício usual deve ser retomado. Algumas vezes, o fato de que é pura
imaginação é descoberto quando a pessoa conversa com aquela pessoa imaginária,
então, deve ser notado como “conversando, conversando.” O verdadeiro objetivo é
de notar cada atividade mental à medida que ela ocorra. Por exemplo, deve ser
notado como “pensamento” no momento em que se estiver pensando e como
“refletindo,” “planejando,” “conhecendo,” “se preocupando,” “se alegrando,”
“sentindo-se preguiçoso,” “sentindo-se feliz,” “sentindo-se enojado,” etc.,
conforme for o caso, à medida que cada atividade ocorra. A contemplação e
notação das atividades mentais é chamada <i>cittanupassana, </i>contemplação da
mente.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Como as pessoas não possuem
conhecimento prático da<span style="mso-spacerun: yes">  </span>meditação <i>vipassana</i>,
elas em geral não estão em posição de compreender o verdadeiro estado da mente.
Isso as conduz naturalmente ao entendimento incorreto de assumir que a mente é
uma “pessoa,” “eu,” “entidade viva.” Elas em geral crêem que a “imaginação” é o
eu,” “eu sou o pensamento,” “eu sou o planejamento,” “eu sou a compreensão” e
assim por diante. Elas acreditam que existe uma entidade viva ou eu que cresce
da infância até a idade adulta. Na realidade, essa entidade viva não existe,
mas há um processo contínuo de elementos mentais que ocorrem um de cada vez, em
sucessão. A prática da meditação é portanto feita com o objetivo de descobrir a
verdadeira natureza deste complexo mente-corpo.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Com respeito à mente e a
maneira como ela surge, o Buda menciona no Dhammapada (verso 37):<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><i>Durangamam ekacaram<br>

Asariram guhasayam<br>

Ye cittam saññamessanti<br>

Mokkhanti Marabandhana.<o:p></o:p></span></i></p>


<p class=Normal style='margin-left:.75in'>Indo longe, perambulando só,<br>

Sem forma e repousando numa caverna.<br>

Aqueles que contêm a mente<br>

Com certeza se libertarão dos grilhões de Mara.<o:p></o:p></span></p>


<p class=Normal><i><span lang=PT-BR style='mso-ansi-language:PT-BR'>Indo
longe.</span></i><span lang=PT-BR style='mso-ansi-language:PT-BR'> A mente em
geral perambula por todo lugar. Enquanto o meditador tenta dar seguimento à
prática de meditação na sala de meditação, com freqüência ele se dá conta de
que a sua mente escapou para muitos outros lugares, cidades, etc. Ele também se
dá conta que a mente num instante de imaginação ou pensamento pode errar para
qualquer um dos lugares distantes que ele tenha conhecido. Esse fato é
descoberto com o auxílio da meditação.<o:p></o:p></span></p>

<p class=Normal><i><span lang=PT-BR style='mso-ansi-language:PT-BR'>Só. </span></i><span
lang=PT-BR style='mso-ansi-language:PT-BR'>A mente ocorre de forma isolada,
momento a momento em sucessão. Aqueles que não percebem essa realidade
acreditam que uma única mente exista no curso da vida ou existência. Eles não
sabem que novas mentes estão sempre surgindo a cada momento. Eles pensam que
ver, ouvir, cheirar, saborear, tocar e pensar no passado e no presente são
parte da mesma mente e que três ou quatro ações de ver, ouvir, tocar, conhecer,
em geral ocorrem ao mesmo tempo.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Esses são entendimentos
incorretos. Na realidade, momentos isolados da mente surgem e desaparecem
continuamente, um após o outro. Isso poderá ser percebido com a prática. Os
casos de imaginação e planejamento são percebidos com facilidade. A imaginação
desaparece assim que for notada como “imaginação, imaginação,” e o planejamento
também desaparece assim que for notado como “planejamento, planejamento.” Essas
ocorrências de surgir, notar e desaparecer se assemelham a um cordão de contas.
A mente que precede não é a mente que segue. Cada uma é separada da outra.
Essas características da realidade podem ser percebidas por qualquer um e para
lograr isso é necessário dar continuidade à prática da meditação.<o:p></o:p></span></p>

<p class=Normal><i><span lang=PT-BR style='mso-ansi-language:PT-BR'>Sem forma. </span></i><span
lang=PT-BR style='mso-ansi-language:PT-BR'>A mente não possui substância, não
possui forma. Não é fácil distinguí-la como é o caso da materialidade. No caso
da materialidade, o corpo, a cabeça, as mãos e as pernas são bastante
proeminentes e notados com facilidade. Se for perguntado o que é a matéria,
esta poderá ser manuseada e mostrada. A mente, no entanto, não é descrita com
facilidade porque não possui substância ou forma. Por essa razão, não é
possível submeter a mente a experimentos analíticos em laboratório.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A pessoa pode, no entanto,
compreender a mente plenamente se for explicado que <i>é aquilo que conhece um
objeto.</i> Para compreender a mente é necessário contemplá-la a cada momento
em que ela ocorre. Quando a meditação estiver avançada de forma razoável, o
acesso da mente ao seu objeto é compreendido com clareza. Parece que a cada
momento mental a mente está realizando um salto direto em direção ao objeto.
Portanto, a meditação é prescrita para conhecer a verdadeira natureza da mente.<o:p></o:p></span></p>

<p class=Normal><i><span lang=PT-BR style='mso-ansi-language:PT-BR'>Repousando numa caverna.
</span></i><span lang=PT-BR style='mso-ansi-language:PT-BR'>Como a mente surge
na dependência da base da mente e das outras portas dos meios dos sentidos
situadas no corpo, diz-se que ela está repousando numa caverna. <o:p></o:p></span></p>

<p class=Normal><i><span lang=PT-BR style='mso-ansi-language:PT-BR'>Aqueles
que contêm a mente com certeza se libertarão dos grilhões de Mara</span></i><span
lang=PT-BR style='mso-ansi-language:PT-BR'>. É dito que a mente deve ser
observada a cada momento em que ela ocorra. A mente pode, portanto, ser
controlada através da meditação. Ao controlar a mente com êxito, o meditador
irá conquistar a libertação dos grilhões de <i>Mara</i>, o Rei da Morte. Com
isso, pode ser visto que é importante notar a mente a cada momento em que ela
ocorre. Assim que for notado, aquele momento mental desaparece. Por exemplo, ao
notar uma ou duas vezes como “intenção, intenção,” observa-se que a intenção
desaparece de imediato. Em seguida o exercício tradicional de notar a
“expansão, contração, expansão, contração” deve ser retomado.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Durante o
exercício usual, a pessoa pode sentir vontade de engolir saliva. Isso deve ser
notado como “desejo,” e ao acumular a saliva como “acumulando” e ao engolir
como “engolindo,” na ordem seqüencial das ocorrências. A razão para a<span
style="mso-spacerun: yes">  </span>contemplação neste caso é porque pode haver
uma idéia pessoal persistente que “querer engolir é o eu,” “engolir também é o
eu.” Na realidade, “desejar engolir” é a mentalidade e não um “eu” e “engolir”
é a materialidade e não um “eu.” Naquele momento apenas existe a mentalidade e
a materialidade. Contemplando desta forma a pessoa irá compreender com clareza
o processo da realidade. Do mesmo modo, no caso de cuspir deve ser notado como
“desejo” quando a pessoa quer cuspir, como “curvando” ao curvar o pescoço, (que
deve ser feito vagarosamente), como “vendo” ao ver e como “cuspindo” ao cuspir.
Em seguida o exercício usual de notar “expansão, contração” deve ser retomado. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ao ficar sentado por um
longo tempo, irão surgir no corpo sensações desagradáveis de tensão, calor e
assim por diante. Essa sensações devem ser notadas quando ocorrerem. A mente deve
ser fixada naquele ponto e a notação de “tenso, tenso” ao sentir tensão, de
“quente, quente” ao sentir calor, de “dor, dor” ao sentir<span
style="mso-spacerun: yes">  </span>dor, de “formigamento, formigamento” ao
sentir sensações de formigamento e de “cansaço, cansaço” ao sentir cansaço. Essa
sensações desagradáveis são <i>dukkha-vedana</i> e a contemplação dessas
sensações é <i>vedananupassana, </i>contemplação das sensações.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Devido à ausência de
conhecimento em relação a essas sensações, persiste o entendimento incorreto de
tomá-las como sendo a própria identidade ou eu, dizendo, “eu sinto a tensão,”
“eu sinto a dor,” “antes eu estava me sentindo bem, mas agora me sinto
desconfortável,” como se fosse um único eu. Na realidade, as sensações
desagradáveis surgem devido a impressões desagradáveis no corpo. Tal como a
luz<span style="mso-spacerun: yes">  </span>numa lâmpada elétrica que poderá
continuar a arder enquanto houver suprimento de energia, o mesmo ocorre com as
sensações que surgem a cada ocasião de contato com impressões desagradáveis.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>É essencial compreender
essas sensações de forma clara. No início ao notar como “tenso, tenso,”
“quente, quente,” “dor, dor,” a pessoa poderá sentir que essas sensações
desagradáveis se intensificam, e em seguida irá notar que surge na mente o
desejo de mudar de postura. Esse desejo deve ser notado como “desejo,
desejo.”<span style="mso-spacerun: yes">  </span>Então, deve-se retornar à
sensação e continuar a notar como “tenso, tenso,”<span style="mso-spacerun:
yes">  </span>ou “quente, quente” e assim por diante. Se, com grande paciência,
a pessoa contemplar dessa forma, as sensações desagradáveis irão desaparecer.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Existe um ditado que diz
que a paciência conduz a <i>Nibbana</i>. É evidente que esse ditado se aplica
mais ao caso da meditação do que qualquer outra coisa. Muita paciência é
necessária no caso da meditação. Se um meditador não for capaz de suportar com
paciência as sensações desagradáveis e com freqüência mudar de posição durante
a meditação, ele não pode esperar atingir alguma concentração. Sem concentração
não existe chance de obter o conhecimento do insight <i>(vipassana-ñana) </i>e
sem o conhecimento do insight os caminhos supramundanos, os seus frutos e <i>Nibbana</i>
não serão realizados.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A paciência é de grande
importância na meditação. A paciência é necessária principalmente para suportar
as sensações desagradáveis no corpo. Raramente existem perturbações externas
que requeiram o exercício da paciência. Isso significa que <i>khantisamvara, </i>a
contenção pela paciência,<i> </i>deve ser observada. A postura não deve ser
mudada de imediato ao surgirem sensações desagradáveis, apesar delas a
meditação deve continuar com as notações das sensações como “tenso, tenso,”
“quente, quente” e assim por diante. Essas sensações dolorosas são algo
absolutamente normal e irão passar. No caso de concentração firme, será
observado que dores intensas irão desaparecer quando forem notadas com
paciência. Com o desaparecimento do sofrimento ou dor, o exercício usual de
notar “expansão, contração” deve ser retomado. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Por outro lado, poderá ser
observado que dores ou sensações desagradáveis não desaparecem de imediato
mesmo quando forem notadas com imensa paciência. Em tal caso, a pessoa não tem
outra alternativa senão mudar de posição. A pessoa deve, nesse caso, submeter-se
às forças superiores. Quando a concentração não for<span style="mso-spacerun:
yes">  </span>firme o suficiente, as dores intensas não irão desaparecer com
rapidez. Nessas circunstâncias, com freqüência surgirá na mente o desejo de
mudar de postura e esse desejo deve ser notado como “desejo, desejo.” Depois
disso, a pessoa deve notar “levantando, levantando” ao mover-se.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>As ações com o corpo devem
ser executadas de forma vagarosa e os movimentos lentos devem ser seguidos e
notados como “levantando, levantando,” “movendo, movendo,” “tocando, tocando,”
na ordem sucessiva do processo. Novamente, ao mover-se a pessoa deve notar
“movendo, movendo” e ao abaixar, notar “abaixando, abaixando.” Se, ao completar
este processo de mudança de postura não houver nada mais para ser notado, o
exercício usual de notar “expansão, contração” deve ser retomado.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Não deve haver interrupção
ou intervalo no processo. O ato precedente de notar e aquele que segue devem
ser contíguos. Da mesma forma, a concentração que precede e aquela que segue
devem ser contíguas e o ato precedente de conhecer e aquele que segue devem ser
contíguos. Desta maneira ocorre o desenvolvimento gradual, em etapas, da
atenção plena, concentração e sabedoria e se houver o seu desenvolvimento
completo, o estágio final de conhecimento do caminho supramundano será
alcançado. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Na prática da meditação <i>vipassana</i>
é importante seguir o exemplo de uma pessoa que tenta acender um fogo. Para
acender um fogo no tempo em que ainda não haviam fósforos, uma pessoa tinha que
friccionar dois gravetos sem que houvesse a menor interrupção no movimento. À
medida que os gravetos se aqueciam, mais esforço era necessário e o ato de
friccionar tinha que ser executado sem cessar. Somente depois que o fogo
tivesse sido produzido a pessoa poderia descansar. Da mesma forma, um meditador
tem que se empenhar duro de forma que não haja interrupção entre a notação
precedente e aquela que segue, entre a concentração precedente e aquela que
segue. Ele deve reverter ao seu exercício usual de notar a “expansão,
contração” depois de haver notado as sensações dolorosas.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Enquanto estiver ocupado
com o seu exercício habitual, ele poderá outra vez sentir coceira em algum
lugar do corpo. Ele deverá, então, fixar a sua atenção no local e fazer a
notação de “coçando, coçando.” A coceira é uma sensação desagradável. Assim que
ela é sentida, surge na mente o desejo de coçar. Esse desejo deve ser notado
como “desejo, desejo,” depois do que, não se deve esfregar ou coçar, mas
deve-se retornar ao ponto da coceira e a notação de “coçando, coçando” deve ser
feita. E ao contemplar desta forma, na maioria dos casos a coceira desaparece e
o exercício usual de notar a “expansão, contração” pode então ser retomado.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Se, por outro lado, for
observado que a coceira não desaparece e que é necessário coçar ou esfregar, a
contemplação dos estágios sucessivos deve ser feita notando a mente como
“desejo, desejo.” Depois deve-se prosseguir notando “levantando, levantando” ao
levantar a mão, “tocando, tocando” quando a mão tocar no ponto, “coçando,
coçando” ou “esfregando, esfregando” quando a mão coçar ou esfregar,
“levantando, levantando” ao levantar a mão, “tocando, tocando” quando a mão
toca o corpo e depois a contemplação usual de “expansão, contração” deve ser
retomada. Em cada caso de mudança de postura a contemplação dos estágios
sucessivos deve ser feita da mesma forma, com cuidado.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Enquanto, assim
cuidadosamente, a pessoa prossegue com a meditação, ela poderá observar que
sensações dolorosas surgem no corpo por sua própria conta. Normalmente as
pessoas mudam a postura assim que sentem a menor sensação desagradável de
cansaço ou calor sem tomar nota desses incidentes. A mudança de postura é feita
de uma forma bastante negligente ao mesmo tempo em que a dor começa a se
intensificar. Assim, as sensações dolorosas não ocorrem de maneira clara. Por
essa razão se diz que, como regra, as posturas escondem as sensações dolorosas.
As pessoas em geral pensam que se sentem bem por dias e noites sem fim. Elas
crêem que as sensações dolorosas apenas ocorrem no momento de um ataque de uma
grave enfermidade.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A realidade é exatamente o
oposto do que as pessoas pensam. Que alguém tente ver quanto tempo consegue
ficar sentado numa única posição sem se mexer. Ela irá sentir desconforto
depois de pouco tempo, digamos cinco ou dez minutos, para em seguida tornar-se
insuportável depois de quinze ou vinte minutos. A pessoa então se sentirá
forçada a mudar de posição quer seja mexendo a cabeça, movendo as mãos ou os
pés, ou movendo o corpo para frente ou para trás. Em geral, muitos movimentos
ocorrem durante um breve intervalo e o seu número seria enorme se fossem
contados ao longo de um único dia.<span style="mso-spacerun: yes">  </span>No entanto,
ninguém parece ter consciência desse fato porque ninguém presta atenção.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso é o que ocorre na
maioria dos casos. Já no caso de um meditador que está sempre plenamente atento
às suas ações e que pratica meditação, as impressões corporais com a sua
respectiva natureza são notadas de forma clara. Não há como as sensações não se
revelarem completamente na sua própria natureza, porque ele permanece com plena
consciência até que elas sejam vistas na íntegra. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Apesar do surgimento de uma
sensação dolorosa, ele permanece observando. Ele normalmente não tenta mudar a
sua postura ou mover-se. Assim, ao surgir na mente o desejo de se mover, de
imediato ele faz a notação disso como “desejo, desejo,” e em seguida retorna
para a sensação dolorosa e continua observando-a. Ele muda a postura ou se mexe
apenas quando a sensação dolorosa for insuportável. Neste caso, ele também
começa pela observação do desejo na mente e prossegue notando com cuidado cada
estágio no processo de mudança. É assim que as mudanças de postura passam a não
esconderem mais as sensações dolorosas. Com freqüência um meditador observa
sensações dolorosas infiltrando-se daqui e dali ou ele poderá sentir sensações
de calor, sensações de dor, coceiras, ou todo o corpo como uma massa de
sensações dolorosas. É assim que se pode observar que as sensações dolorosas
são predominantes pois, as mudanças de postura já não as escondem.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Se ele intenciona mudar a
postura de sentado para em pé, deveria primeiro notar a intenção na mente como
“intenção, intenção” e prosseguir com a disposição das mãos e pernas nos
estágios sucessivos notando “levantando,” “movendo,” “esticando,” “tocando,”
“pressionando” e assim por diante. Quando o corpo se inclina para a frente deve
ser notado como “inclinando, inclinando.” Durante o processo de ficar em pé
ocorre no corpo uma sensação de leveza juntamente com o ato de levantar. A
atenção deve ser fixada nesses fatores e a notação “levantando, levantando”
deve ser feita. O ato de levantar-se deve ser feito vagarosamente.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Durante a prática, o mais
adequado é que o meditador aja em todas atividades com delicadeza e
vagarosamente como se fosse uma pessoa fraca, enferma. Talvez o caso de uma
pessoa que sofra de lumbago seja o exemplo mais adequado. O paciente precisa
ser sempre cuidadoso e mover-se vagarosamente para evitar as dores. Da mesma
forma, um meditador deve sempre tentar fazer os movimentos da forma mais lenta
possível. O movimento lento é necessário para possibilitar que a atenção plena,
concentração e sabedoria possam acompanhá-lo. Vivendo o tempo todo de forma
descuidada, agora a pessoa apenas começa a se treinar seriamente para manter a
mente no corpo. É apenas o início e a atenção plena, concentração e sabedoria
ainda não estão adequadamente preparadas enquanto que os processos físicos e
mentais continuam a ocorrer na velocidade máxima. Por isso, é imperativo
reduzir a velocidade desses processos ao nível mínimo, para possibilitar que a
atenção plena e a sabedoria os acompanhe. Portanto, vale a pena executar os
exercícios em câmara lenta durante todo o tempo.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Além disso, é aconselhável
que o meditador se comporte como uma pessoa cega durante o período de
treinamento. Uma pessoa sem autocontrole não possui uma aparência digna porque
em geral ela olha para as coisas e pessoas de forma libertina. Ela também será
incapaz de obter um estado mental calmo e estável. A pessoa cega, por outro
lado, se comporta de uma forma contida sentando-se com serenidade com os olhos
abaixados. Ela nunca se vira para os lados para olhar para coisas ou pessoas
porque ela é cega e é incapaz de vê-las. Mesmo que uma pessoa se aproxime e lhe
diga algo, ela nunca se vira para olhar para aquela pessoa. Essa atitude
contida é digna de ser imitada. Um meditador deveria agir da mesma forma
durante a prática da meditação. Ele não deveria olhar para nada. A sua mente
deveria estar apenas focada no objeto da meditação. Enquanto estiver sentado
ele deve notar com atenção “expansão, contração.” Mesmo se coisas estranhas
ocorrerem nas proximidades, ele não deveria olhar para elas. Se acontecer dele
ver algo, deve somente fazer uma notação “vendo, vendo” e prosseguir com o
exercício usual de notar a “expansão, contração.” Um meditador deve ter um
grande respeito por este exercício e executá-lo com toda consideração, a ponto
de ser confundido com uma pessoa cega.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A esse respeito, foi
percebido que algumas mulheres, meditadoras, tiveram um comportamento perfeito.
Elas realizaram o exercício com todo respeito de acordo com as instruções. Os
seus modos foram contidos e elas sempre se aplicaram ao objeto da meditação.
Elas nunca olharam em volta. Ao caminhar elas sempre estiveram atentas aos seus
passos. Os passos foram leves, suaves e lentos. Todos os meditadores deveriam
seguir o exemplo delas.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Também é necessário que um
meditador se comporte como uma pessoa surda. Normalmente, assim que uma pessoa
ouve um som, ela se vira e olha para a direção de onde veio o som ou ela se
vira para a pessoa que lhe disse algo, para responder. Ela não se comporta de
forma serena. Uma pessoa surda, por outro lado, se comporta de forma serena.
Ela não dá atenção a nenhum som ou conversa porque ela nunca os ouve. Um
meditador deve se comportar da mesma forma sem dar atenção a conversas sem
importância, nem deveria de forma deliberada ouvir qualquer conversa ou
discurso. Se acontecer dele ouvir um som ou conversa, ele deveria de imediato
fazer uma notação “ouvindo, ouvindo” e prosseguir com o exercício usual de
notar a “expansão, contração.” Ele deveria executar este exercício com toda
consideração, a ponto de ser confundido com uma pessoa surda.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Deve ser lembrado que a <i>única
</i>preocupação de um meditador é se dedicar com atenção à meditação. As demais
coisas vistas ou ouvidas não lhe dizem respeito. Mesmo que elas aparentem ser
estranhas ou interessantes, ele não deve dar-lhes atenção. Ao ver algo, ele
deve ignorá-lo como se não o visse. Assim também, ele deve ignorar vozes ou
sons como se não os ouvisse. No caso de ações com o corpo, ele deve agir de
forma lenta e delicada como se estivesse enfermo ou muito fraco.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Outros Exercícios<o:p></o:p></span></b></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Caminhar<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Deve ser enfatizado,
portanto, que o ato de erguer o corpo para ficar em pé deve ser executado
vagarosamente. Ao chegar na posição ereta, uma notação “em pé, em pé” deve ser
feita. Se por acaso a pessoa olhar em volta, uma notação “olhando, vendo” deve
ser feita e ao caminhar, cada passo deve ser notado como “pé direito, pé
esquerdo” ou “caminhando, caminhando.” A cada passo, a atenção deve ser fixada
na sola do pé ao movê-la a partir do ponto em que a perna é erguida, até o
ponto em que é colocada no chão.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ao caminhar com passos
rápidos ou numa caminhada longa, será suficiente uma notação para uma seção de
cada passo como “direito, esquerdo” ou “caminhando, caminhando.”<span
style="mso-spacerun: yes">  </span>No caso de caminhar lentamente, cada passo
pode ser dividido em três seções – levantando, movendo para a frente e
abaixando. Ao iniciar o exercício, a notação de cada passo em duas partes deve
ser feita como “levantando” ao fixar a atenção no movimento ascendente do pé,
do princípio ao fim, e como “abaixando” ao fixar no movimento descendente, do
princípio ao fim. Assim, o exercício que começa com o primeiro passo notando
“levantando, abaixando,” termina agora.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Normalmente, quando o pé é
abaixado e a notação “abaixando” é feita, a outra perna começa a se levantar
para iniciar o passo seguinte. Isso não deve ocorrer. O próximo passo deve
começar apenas depois que o passo anterior tenha terminado, assim: “levantando,
abaixando” para o primeiro passo e “levantando, abaixando” para o segundo
passo. Depois de dois ou três dias, este exercício se tornará muito fácil e
então o meditador deverá realizar o exercício de notar cada passo em três
seções como “levantando, movendo, abaixando.” Por enquanto, os meditadores
devem iniciar o exercício notando “direito, esquerdo” ou “caminhando,
caminhando” ao caminhar rapidamente, e notando “levantando, abaixando” ao
caminhar lentamente. <o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Sentar </span></b><span
lang=PT-BR style='mso-ansi-language:PT-BR'><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Enquanto estiver
caminhando, a pessoa pode sentir o desejo de sentar. A pessoa deve então fazer
a notação “desejo.”<span style="mso-spacerun: yes">  </span>Se acontecer da
pessoa olhar para cima, deve ser notado como “olhando, vendo, olhando, vendo”;
ao se dirigir para o assento como “levantando, abaixando”; ao parar como
“parando, parando”; ao dar a volta como “virando, virando.” Ao sentir o desejo
de sentar, notá-lo como “desejo, desejo.” No ato de sentar ocorre no corpo uma
sensação de peso e também um puxão para baixo. A atenção deve ser fixada nesses
fatores e a notação “sentando, sentando, sentando” deve ser feita. Depois de
haver sentado existirão movimentos de colocar as mãos e pernas em posição.
Estes devem ser notados como “movendo,” “dobrando,” “esticando” e assim por
diante. Se não houver nada para fazer e a pessoa estiver calmamente sentada,
ela deve retomar o exercício usual de notar “expansão, contração.”<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Deitar<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Se no transcurso da
meditação a pessoa sentir dores ou cansaço, ou calor, deve ser feita uma
notação disso e depois reverter ao exercício usual de notar “expansão,
contração.” Se a pessoa sentir sonolência, deve fazer a notação “sonolência,
sonolência” e prosseguir com a notação de todos os atos da preparação para se
deitar: note trazer as mãos e as pernas para uma outra posição como
“levantando,” “pressionando,” “movendo,” “apoiando”; ao inclinar o corpo como
“inclinando, inclinando”; ao esticar as pernas como “esticando, esticando”; e
ao descer o corpo e deitar como “deitando, deitando, deitando.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Esses atos insignificantes
ao deitar também são importantes e não devem ser negligenciados. Existe uma
real possibilidade de obter a iluminação durante esse breve intervalo. Com o
pleno desenvolvimento da concentração e da sabedoria, a iluminação pode ser
alcançada durante um movimento de dobrar ou esticar. Foi assim que o Venerável
Ananda alcançou o estado de <i>Arahant</i> no exato momento em que se deitava.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Por volta do início do
quarto mês após a morte do Buda, foi organizado o primeiro concílio de <i>bhikkhus</i>
para que em conjunto classificassem, examinassem, confirmassem e recitassem
todos os ensinamentos do Buda. Naquela ocasião, quinhentos <i>bhikkhus</i>
foram escolhidos para realizar essa tarefa. Desses <i>bhikkhus</i> quatrocentos
e noventa e nove eram <i>Arahants</i> enquanto que o venerável Ananda era um <i>sotapanna,
‘</i>aquele que entrou na correnteza’.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Para participar do concílio
como um <i>Arahant</i>, em pé de igualdade com os demais, ele fez um tremendo
esforço de meditação no dia anterior ao início do concílio. Isso ocorreu no
quarto minguante do mês de <i>Savana</i> (Agosto). Ele praticou a atenção plena
no corpo e continuou com a meditação andando por toda a noite. Pode ter sido
com o mesmo exercício de notar “esquerdo, direito” ou “caminhando, caminhando.”
Ele estava dessa forma ocupado com a intensa contemplação do processo de
mentalidade e materialidade em cada passo até o amanhecer do dia seguinte e
ainda assim sem alcançar o estado de <i>Arahant</i><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então o venerável Ananda
pensou: “Eu fiz o máximo que pude. O Buda disse: ‘Ananda, você possui todas as
perfeições <i>(paramis)</i>. Pratique a meditação. Com certeza você irá um dia
alcançar o estado de <i>Arahant</i>.’ Eu fiz o melhor que pude, tanto assim que
sou um daqueles que fizeram o melhor que podiam na meditação. Qual seria a
razão para o meu fracasso?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então ele se lembrou: “Ah!
Eu fui demasiadamente ávido ao praticar apenas a meditação andando durante toda
a noite. Existe um excesso de energia e falta de concentração que na verdade é
a razão para este estado de inquietação. Agora é necessário parar a meditação
andando para equilibrar a energia e a concentração e continuar com a meditação
numa posição deitada.” O venerável Ananda entrou então no seu quarto, sentou-se
na cama e começou a deitar. Conta-se que ele alcançou o estado de <i>Arahant</i>
no exato momento em que se deitava, ou melhor dizendo, no momento em que
contemplava “deitando, deitando.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Essa forma de alcançar o
estado de <i>Arahant</i> foi registrada nos Comentários como um evento estranho
porque se encontra fora das quatro posturas normais de ficar em pé, sentado,
deitado e caminhando. No momento da sua iluminação, não se pode estritamente
dizer que o venerável Ananda se encontrava em pé porque os seus pés não estavam
sobre o chão, nem estava sentado porque o seu corpo já estava inclinado,
estando próximo ao travesseiro, nem poderia estar deitado pois a sua cabeça
ainda não havia tocado o travesseiro e o seu corpo ainda não estava na horizontal.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O venerável Ananda era um
‘que entrou na correnteza’ e por isso ele precisaria desenvolver os outros três
estágios superiores – o caminho e fruto daquele que retorna uma vez, o caminho
e fruto daquele que não retorna e o caminho e fruto do <i>Arahant</i> como
realização final. Para realizar tudo isso foi necessário apenas um instante.
Portanto, é necessário muito cuidado para manter a prática da meditação sem
relaxamento ou omissão.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>No ato de deitar, a
contemplação deve ser realizada com muito cuidado. Quando um meditador se sente
sonolento e quer se deitar deve ser feita a notação “sonolência, sonolência,”
“desejando, desejando”; ao levantar a mão “levantando, levantando”; ao esticar
“esticando, esticando”; ao tocar “tocando, tocando”; ao pressionar “pressionando,
pressionando”; depois de inclinar o corpo e deitá-lo “deitando, deitando.” O
ato de deitar-se deve ser feito vagarosamente. Ao tocar o travesseiro deve ser
notado “tocando, tocando.” Existem muitos lugares no corpo com sensações
tácteis mas, cada ponto deve ser notado apenas um de cada vez.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Na posição deitada também
existem muitos movimentos do corpo ao colocar as mãos e pernas em posição.
Essas ações devem ser notadas cuidadosamente como “levantando,” “esticando,”
“dobrando,” “movendo” e assim por diante. Ao virar o corpo a notação “virando,
virando” deve ser feita e quando não houver nada em particular para ser notado,
o meditador deve manter a prática usual de notar “expansão, contração.”
Enquanto a pessoa estiver deitada de costas ou de lado, em geral, não há nada
em particular para ser notado e o exercício usual “expansão, contração” deve
ser mantido.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Pode acontecer que enquanto
deitado surjam muitas oportunidades para que a mente escape. Essa mente que
escapa deve ser notada como “escapando, escapando,” ao escapar, como “chegando,
chegando” quando chega a algum lugar, como “planejando,” “refletindo” e assim
por diante para cada estado, da mesma maneira como na meditação na posição
sentada. Os estados mentais desaparecem ao serem notados por uma ou duas vezes.
O exercício usual de notar a “expansão, contração” deve ser retomado. Também
pode haver ocorrências de engolir ou cuspir saliva, sensações dolorosas,
sensações de calor, sensações de coceira, etc., ou de ações com o corpo ao
mudar de posição, ou mover os membros. Estas devem ser contempladas à medida
que ocorrem. (Quando a concentração estiver firme o suficiente, será possível
manter a contemplação de cada ato de abrir e fechar as pálpebras e piscar).
Depois, quando não há mais nada para ser notado deve-se retornar ao exercício
usual.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Dormir<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Embora tarde da noite na
hora de dormir, não é aconselhável que se abandone a meditação para ir dormir.
Qualquer um que esteja seriamente interessado na meditação deve estar preparado
para enfrentar a possibilidade de ficar várias noites sem dormir.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>As escrituras são enfáticas
sobre a necessidade de se desenvolver as qualidades dos quatro fatores da
energia <i>(caturanga-viriya) </i>na prática de meditação: “Na dura contenda, a
pessoa pode ficar reduzida a uma mera armação de pele, ossos e tendões quando o
sangue e os músculos murcharem e secarem, mas ela não deve abandonar o empenho
enquanto não tiver alcançado aquilo que pode ser alcançado através da
perseverança, energia e esforço.” Essas instruções devem ser seguidas com muita
determinação. É possível permanecer desperto se houver concentração firme o
suficiente para repelir o sono, mas a pessoa irá adormecer se o sono levar
vantagem.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando a pessoa se sentir
sonolenta, ela deverá notar isso como “sonolência, sonolência”; quando as
pálpebras estiverem pesadas como “pesado, pesado”; quando os olhos estiverem
ofuscados como “ofuscado, ofuscado.” Depois de contemplar dessa forma, pode ser
que ela consiga espantar o sono e volte a sentir-se novamente desperta. Essa
sensação deve ser notada como “desperta, desperta,” depois do que, o exercício
usual de notar “expansão, contração” deve ser retomado. No entanto, apesar
dessa determinação, a pessoa pode sentir que é incapaz de manter-se desperta se
estiver muito sonolenta. Na posição deitada é mais fácil adormecer. Um
principiante deveria portanto tentar permanecer sentado e andando.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando a noite tiver
avançado, no entanto, o meditador poderá se sentir forçado a deitar e continuar
com a contemplação da expansão e contração. Nessa posição ele provavelmente irá
dormir. Enquanto estiver dormindo, não é possível continuar com a tarefa da
contemplação. É um intervalo para que o meditador relaxe. Uma hora de sono irá
lhe proporcionar uma hora de relaxamento e se ele continuar a dormir por duas,
três ou quatro horas, ele irá estar ainda mais relaxado, mas não é aconselhável
que um meditador durma mais de quatro horas que é o tempo suficiente para um
sono normal.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Despertar<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Um meditador deve começar a
contemplação no momento em que desperta. Estar plenamente ocupado com intensa
contemplação durante as horas em que esteja desperto deve ser a rotina de um
meditador que se empenha com afinco e que tem a verdadeira aspiração de
alcançar o caminho supramundano e o seu fruto. Se não for possível detectar o
momento do despertar, então ele deve começar com o exercício usual de notar a
“expansão, contração.” Se ele<span style="mso-spacerun: yes">  </span>primeiro
tomar consciência de uma reflexão, ele deve começar a contemplação notando
“refletindo, refletindo” e depois retomar o exercício usual de notar a
“expansão, contração.” Se ele primeiro tomar consciência de uma voz ou um outro
som, ele deve começar notando “ouvindo, ouvindo” e depois retomar o exercício
usual de notar a “expansão, contração.” Ao despertar podem ocorrer movimentos
com o corpo ao virar para este ou aquele lado, movendo as mãos ou pernas e
assim por diante. Essas ações devem ser contempladas em ordem sucessiva.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Se ele primeiro tomar
consciência de estados mentais que conduzam a várias ações com o corpo, ele
deve começar a contemplação notando a mente. Se ele primeiro tomar consciência
de sensações dolorosas, ele deve começar a contemplação notando essas sensações
dolorosas e depois continuar com a notação das ações com o corpo. Se ele
permanecer quieto, sem se mover, o exercício usual de notar a “expansão,
contração” deve ser retomado. Se ele tiver a intenção de se levantar, ele deve
notar isso como “intencionando, intencionando” e depois prosseguir com a
notação de todas as ações em ordem seqüencial ao colocar as mãos e pernas nas
suas posições. A pessoa deve notar “levantando, levantando” ao levantar o
corpo, “sentando, sentando” quando o corpo estiver ereto e numa posição
sentada, e também deve-se notar todas as demais ações para colocar as pernas e
mãos nas suas posições. Se não houver nada em particular para ser notado, então
o exercício usual de notar a “expansão, contração” deve ser retomado.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Até agora mencionamos as
coisas que dizem respeito aos objetos de contemplação conectados com as quatro
posturas e as mudanças de uma para outra. Isto é uma mera descrição do esboço
geral dos principais objetos de contemplação que devem ser observados ao longo
da prática. No entanto, no início da prática é difícil seguir todos eles
durante a meditação. Muitas coisas serão omitidas mas, ao obter uma
concentração firme o suficiente será fácil seguir durante a meditação não
somente os objetos enumerados, mas muitos outros. Com o desenvolvimento gradual
da atenção plena e da concentração, o progresso da sabedoria se acelera e dessa
forma muito mais objetos podem ser percebidos. É necessário trabalhar, fazendo
um esforço para alcançar esse nível elevado.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Banhar-se e Comer<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A contemplação deve ser
mantida ao lavar o rosto pela manhã ou ao tomar um banho. Como é necessário
agir com rapidez nessas ocasiões devido à natureza do próprio ato, a
contemplação deve ser feita de acordo com as circunstâncias. Ao esticar a mão
para pegar o sabonete, deve ser notado como “esticando, esticando”; ao pegar o
sabonete como “pegando, pegando”; ao molhar como “molhando, molhando”; ao
trazer o sabonete para o corpo como “trazendo, trazendo”; ao despejar água
sobre o corpo ou a face como “despejando, despejando”; ao sentir frio como
“frio, frio”; ao esfregar como “esfregando, esfregando” e assim por diante.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Também pode haver muitas
ações diferentes com o corpo ao trocar ou arrumar a roupa, ao arrumar ou trocar
a cama, ao abrir a porta, e assim por diante. Essas ações devem ser
contempladas na seqüência, em detalhe, tanto quanto possível.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ao tomar uma refeição, a
contemplação deve começar no momento em que se olhe para a mesa notando<span
style="mso-spacerun: yes">  </span>“olhando, vendo, olhando, vendo”; ao esticar
a mão para o prato, como “esticando, esticando”; quando a mão toca o alimento,
como “tocando, quente, quente”; ao juntar a comida, como “juntando, juntando”;
ao pegar a comida, como “pegando, pegando”; depois de erguer a mão e ao
levantá-la, como “trazendo, trazendo”; ao dobrar o pescoço para a frente, como
“dobrando, dobrando”; ao colocar a comida na boca, como “colocando, colocando”;
ao retirar a mão, como “retirando, retirando”; ao tocar o prato com a mão, como
“tocando, tocando”; ao endireitar o pescoço, como “endireitando, endireitando”;
ao mastigar a comida, como “mastigando, mastigando”; ao saborear a comida, como
“saboreando, saboreando”; ao apreciar o sabor, como “apreciando, apreciando”;
ao gostar do sabor, como “gostando, gostando”; ao engolir, como “engolindo,
engolindo.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Essa é uma ilustração da
rotina de contemplação ao tomar cada bocado de comida até que a refeição esteja
terminada. Neste caso também é difícil seguir todas as ações no início da
prática. Haverá muitas omissões. Apesar disso, os meditadores não devem
hesitar, mas, devem tentar prosseguir tanto quanto puderem. Com o gradual
progresso na prática, será mais fácil notar muito mais objetos do que aqueles
aqui mencionados.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>As instruções para os
exercícios práticos de meditação estão quase completas. Como elas foram
explicadas em detalhe já há algum tempo, não será fácil lembrar de todas. Para
facilitar a memória, será dado um breve sumário dos pontos essenciais e
importantes.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Sumário dos Pontos
Essenciais<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ao caminhar, o meditador
deve contemplar os movimentos de cada passo. Ao caminhar depressa, cada passo
deve ser notado como “direito, esquerdo” respectivamente. A mente deve estar
fixada com atenção na sola dos pés, nos movimentos de cada passo. Ao caminhar
vagarosamente, cada passo deve ser notado em duas partes como “levantando,
abaixando.” Enquanto estiver sentado, o exercício usual de meditação deve ser
feito notando os movimentos do abdômen como “expansão, contração, expansão,
contração.”<span style="mso-spacerun: yes">  </span>O mesmo tipo de
contemplação de notar os movimentos como “expansão, contração, expansão,
contração” deve ser feito enquanto estiver deitado.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Se for observado que a
mente escapa durante o processo de notar a “expansão, contração,” não se deve
permitir que ela continue perambulando e isso deve ser notado de imediato. Ao
imaginar,<span style="mso-spacerun: yes">  </span>isso deve ser notado como
“imaginando, imaginando”; ao pensar, como “pensando, pensando”; a mente
escapando, como “escapando, escapando”; e a mente ao chegar num lugar, como
“chegando, chegando” e assim por diante para cada ocorrência e em seguida o
exercício usual de notar a “expansão, contração” deve ser retomado.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ao ocorrerem sensações de cansaço
nas mãos, pernas ou outros membros, ou sensações de calor, formigamento, dor ou
coceira, estas devem ser observadas e notadas como “cansaço,” “calor,”
“formigamento,” “dor,” “coceira,” e assim por diante conforme for o caso. O
exercício usual de notar a “expansão, contração” deverá em seguida ser
retomado. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ao ocorrerem as ações de
curvar ou esticar as mãos ou pernas, ou mover o pescoço ou membros, ou inclinar
o corpo para frente e para trás, estes devem ser observados na ordem seqüencial
à medida que ocorrerem. O exercício usual de notar a “expansão, contração”
deverá em seguida ser retomado.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isto é apenas um resumo.
Quaisquer outros objetos que devam ser contemplados durante o período de
treinamento serão mencionados pelos instrutores durante a entrevista diária com
os discípulos, quando são dadas as instruções de meditação.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Se a pessoa praticar da
maneira indicada, o número de objetos aumentará gradualmente com o tempo. No
início haverá muitas omissões, pois a mente está habituada a perambular sem qualquer
tipo de restrição. No entanto, um meditador não deve desistir por isso. Esse
tipo de dificuldade é muito comum no início da prática. Depois de algum tempo a
mente não poderá se evadir porque sempre será descoberta cada vez que queira
escapar. Assim, ela permanecerá fixa no objeto para o qual ela está dirigida.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ao surgir um objeto a mente
o registra e dessa forma o objeto e a mente coincidem. Com o desaparecimento do
objeto a mente registra isso e dessa forma o objeto e a mente coincidem. Existe
sempre um par, o objeto e a mente que conhece o objeto, a cada momento em que
uma notação é feita. Esses dois elementos, o objeto e a mente que conhece,
sempre surgem em pares, e, exceto por esses dois elementos, não existe nenhuma
outra coisa sob a forma de uma pessoa ou eu. Essa realidade será compreendida
pessoalmente no seu devido tempo.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O fato de que a
materialidade e a mentalidade são duas coisas distintas, separadas, será
percebido com clareza durante o processo de notar a “expansão, contração.” Os
dois elementos de materialidade e mentalidade estão conectados em pares e o seu
surgimento coincide, isto é,<span style="mso-spacerun: yes">  </span>surgindo o
processo de materialidade, surge junto o processo de mentalidade que o conhece.
Ao desaparecer o processo de materialidade, desaparece junto o processo de
mentalidade que o conhece. O mesmo acontece com o processo de levantar, mover,
abaixar: esses são processos de materialidade que surgem e desaparecem junto
com os processos de mentalidade que os conhece. Esse conhecimento da<span
style="mso-spacerun: yes">  </span>materialidade e da mentalidade surgindo
separadamente é denominado <i>nama-rupa-pariccheda-ñana, </i>o conhecimento que
discrimina<span style="mso-spacerun: yes">  </span>mentalidade-materialidade. É
o estágio preliminar em todo o processo de conhecimento meditativo. É
importante desenvolver este estágio preliminar de maneira apropriada.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Dando continuidade à
prática de meditação durante algum tempo, haverá progresso considerável na
atenção plena e concentração. Nesse nível será percebido que a cada notação,
cada processo surge e desaparece naquele exato momento. Mas, por outro lado, as
pessoas sem instrução em geral consideram que o corpo e a mente continuam num
estado permanente por toda a vida, que o mesmo corpo da infância cresceu até a
idade adulta, que a mesma mente jovem cresceu até a maturidade e que ambos, o
corpo e a mente, são um e a mesma pessoa. Na realidade, não é assim. Nada é
permanente. Tudo surge por um momento e depois desaparece. Nada permanece
imutável nem mesmo<span style="mso-spacerun: yes">  </span>durante um piscar
dos olhos. As mudanças ocorrem com muita rapidez e estas serão percebidas no
seu devido tempo.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ao praticar a meditação
notando a “expansão, contração” e assim por diante, a pessoa irá perceber que
esses processos surgem e desaparecem um após o outro em rápida sucessão. Ao
perceber que tudo desaparece no ponto exato da notação, o meditador compreende
que nada é permanente. O conhecimento relativo à natureza impermanente das
coisas é <i>aniccanupassana-ñana, </i>o conhecimento meditativo da
impermanência.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O meditador então
compreende que esse estado de mudança constante é insatisfatório e não é algo
que deva ser desejado. Isso é <i>dukkhanupassana-ñana, </i>o conhecimento
meditativo do sofrimento. Ao sofrer com as muitas sensações dolorosas, este
complexo de mente e corpo é visto como um mero aglomerado de sofrimento. Isto
também é o conhecimento<span style="mso-spacerun: yes">  </span>meditativo do
sofrimento.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Neste ponto, ele percebe
que os elementos da materialidade e mentalidade nunca obedecem aos desejos da
pessoa, mas, surgem de acordo com a sua própria natureza e condições. Ao se
ocupar com a notação desses processos, o meditador compreende que esses
processos não são controláveis e que eles não são nem uma pessoa, nem uma
entidade viva, nem um eu. Isto é <i>anattanupassana-ñana, </i>o conhecimento
meditativo de não-eu.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando o meditador tiver
completa compreensão do conhecimento da impermanência, sofrimento e não-eu, ele
terá realizado Nibbana. Desde tempos imemoriais, Budas, <i>Arahants</i> e <i>Ariyas</i>
(nobres) têm realizado <i>Nibbana</i> através deste método de <i>vipassana</i>.
No caminho que realiza <i>Nibbana</i>, <i>vipassana</i> consiste de quatro <i>satipatthana,
</i>aplicações ou fundamentos da atenção plena, e <i>satipatthana </i>é na
verdade o caminho que realiza <i>Nibbana</i>.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os meditadores que seguem
este método de treinamento devem se lembrar que eles estão no caminho que já
foi percorrido por Budas, <i>Arahants</i> e <i>Ariyas</i>. Esta oportunidade
lhes é oferecida devido aos seus <i>parami, </i>isto é, os seus esforços
anteriores buscando e desejando isso e também devido às condições de maturidade
espiritual deles. Eles devem se alegrar por terem esta oportunidade. Eles devem
também ter a certeza de que percorrendo este caminho sem hesitação, obterão a
experiência pessoal da concentração e sabedoria altamente desenvolvidas, como
anteriormente conhecidos pelos Budas, <i>Arahants</i> e <i>Ariyas</i>. Eles
desenvolverão um estado de concentração tão puro como nunca conhecido antes e
como resultado da concentração avançada irão desfrutar de muitos prazeres
bem-aventurados.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A impermanência, sofrimento
e não-eu serão compreendidos através da experiência pessoal direta, e com a
completa compreensão desses conhecimentos, <i>Nibbana</i> será realizado. Não
tardará muito para alcançar o objetivo, talvez um mês, ou vinte dias, ou quinze
dias, ou, em ocasiões raras, até mesmo em sete dias para aqueles poucos com <i>parami
</i>extraordinários.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os meditadores devem,
portanto, se dedicar à prática da meditação com toda seriedade e com plena
confiança, acreditando que, com certeza, ela os conduzirá ao desenvolvimento
dos nobres caminhos supramundanos e os seus frutos e à realização de <i>Nibbana</i>.
Eles, assim, se verão livres do entendimento incorreto do eu e da dúvida, e não
mais estarão sujeitos ao ciclo de renascimentos nos estados miseráveis dos
infernos, reino animal e na esfera dos <i>petas</i>.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Que os meditadores sejam
bem sucedidos nesse nobre esforço.<o:p></o:p></span></p>

