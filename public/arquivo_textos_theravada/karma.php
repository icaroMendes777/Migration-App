<p class=Normal><i><span lang=PT-BR style='mso-ansi-language:PT-BR'>Karma</span></i><span
lang=PT-BR style='mso-ansi-language:PT-BR'> é uma daquelas palavras que não traduzimos.
O seu significado básico é bastante simples - <i>ação -</i> mas devido à
importância que os ensinamentos do Buda atribuem ao papel da ação, a palavra <i>karma
</i>em Sânscrito contém tantas implicações que a palavra <i>ação </i>em
Português não consegue abarcar todo o seu conteúdo. É por essa razão que
simplesmente absorvemos a palavra original como parte do nosso vocabulário. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Porém quando tentamos
identificar todas as conotações que a palavra contém, agora que ela se
incorporou à linguagem do dia a dia, nos damos conta que o seu verdadeiro
significado não está claro. Aos olhos da maioria da pessoas, karma funciona
como destino - má sorte, uma força inexplicável e imutável que surge do nosso
passado,<span style="mso-spacerun: yes">  </span>pela qual somos responsáveis,
ainda que vagamente e que não temos forças para resistir. &quot;Creio que deve
ser o meu karma,&quot; ouvi pessoas dizerem quando atingidas pela má sorte com
tal intensidade que não viram outra alternativa senão aceitar com resignação. O
fatalismo implícito nessa afirmação é uma das razões porque tantos de nós
rejeitamos o conceito de karma, pois soa tal como o tipo de mito insensível que
pode justificar praticamente qualquer tipo de sofrimento ou injustiça na
sociedade: &quot;Se ele é pobre, é devido ao seu karma.&quot; &quot;Se ela foi
estuprada, é por causa do seu karma.&quot; A partir daí é só um pequeno passo
para dizer que ele ou ela <i>merecem</i> sofrer e dessa forma não merecem a
nossa ajuda. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Esse entendimento incorreto
vem do fato de que o conceito Budista de karma veio para o Ocidente ao mesmo
tempo que conceitos não Budistas e dessa forma acabou herdando uma bagagem
indevida. Apesar de muitos conceitos de karma na Ásia serem fatalistas, o
conceito Budista original não era de forma alguma fatalista. Na verdade, se
analisarmos com atenção as idéias originais do Budismo acerca de karma, veremos
que elas dão ainda menos importância a mitos do passado que a maioria das
pessoas no Ocidente. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>No Budismo original, o
karma não era linear. Outras escolas Hindus acreditavam que o karma operava
como uma linha reta, com ações do passado influenciando o presente, e ações no
presente influenciando o futuro. Como resultado, eles viam pouco espaço para a
livre escolha. Os Budistas no entanto, viram que o karma opera através do
processo de feedback, com o momento presente sendo determinado tanto por ações
do passado como do presente, as ações do presente influenciam não somente o
futuro mas também o presente. Essa constante abertura para a influência da ação
no presente no processo causal torna possível a livre escolha. Essa liberdade
está simbolizada na imagem que os Budistas usam para explicar o processo: a
água corrente. Em certas ocasiões a torrente que flui do passado é tão forte
que pouco pode ser feito exceto manter-se firme no lugar, porém existem também
ocasiões em que a torrente é suficientemente fraca e pode ser desviada quase
que para qualquer direção.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Dessa forma, ao invés de
promover a resignação impotente, a noção de karma no Budismo original focava no
potencial libertador daquilo que a mente está fazendo a cada momento. Quem você
é - de onde você veio - não se compara em termos de importância àquilo que a
mente está fazendo a cada momento. Quem você é - de onde você veio - não se
compara em termos de importância aos motivos da mente para fazer aquilo que
está fazendo agora. Mesmo que o passado possa ser responsável por muitas das
desigualdades que vemos na vida, a nossa medida como seres humanos não é aquilo
que a sorte nos deu pois essa sorte pode mudar a cada momento. A nossa medida
se estabelece pela maneira como lidamos com a sorte que temos. Se você estiver
sofrendo, você tenta evitar continuar com os hábitos mentais inábeis que farão
com que esse feedback cármico em particular se mantenha. Se você vê que outras
pessoas estão sofrendo, e você pode ajudá-las, você não foca no passado cármico
delas mas na sua oportunidade cármica no presente. Algum dia você poderá se
encontrar na mesma situação em que elas estão agora, assim, essa é a
oportunidade para agir da forma como você gostaria que elas agissem com relação
a você quando esse dia chegar. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Essa crença de que a
dignidade de uma pessoa é medida não pelo seu passado mas pelas suas ações no
presente foi frontalmente contrária à tradição Hindu de hierarquia baseada em
castas e, explica porque no Budismo original existe tanto humor acerca da
arrogância e da mitologia dos brâmanes. Tal como apontado pelo Buda, um brâmane
podia ser uma pessoa superior não porque ele nascera de um ventre brâmane mas
somente se ele agisse com verdadeiras intenções hábeis. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Sempre que lemos acerca dos
ataques dos primeiros Budistas ao sistema de castas, excluindo as suas
sugestões anti-racistas, eles parecem ser inapropriados. Mas não nos damos
conta de que eles atingem bem no cerne dos nossos mitos acerca do nosso próprio
passado; nossa obsessão em definir quem somos de acordo com nossas origens -
nossa raça, herança étnica, sexo, referencial sócioeconomico, preferência
sexual -- nossas tribos modernas. Colocamos uma quantidade desproporcional de
energia na criação e manutenção da mitologia da nossa tribo de modo que
possamos nos orgulhar acerca da boa reputação da nossa tribo. Mesmo quando nos
tornamos Budistas, a tribo tem prioridade. Nós exigimos um Budismo que honre os
nossos mitos. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Do ponto de vista de karma
no entanto, de onde viemos é karma antigo sobre o qual não temos controle. O
que &quot;somos&quot; é na melhor hipótese um conceito nebuloso - e na pior
hipótese prejudicial quando o utilizamos como desculpa para agir com motivos
inábeis. O valor de uma tribo se encontra somente nas ações hábeis dos seus
membros. Mesmo quando essas pessoas de bem pertencem à nossa tribo, o bom karma
delas pertence a elas, não a nós. E, é lógico, toda tribo tem os seus membros
ruins, o que significa que a mitologia da tribo é algo frágil. Apegar-se a algo
frágil requer um grande investimento em termos de cobiça, raiva e delusão,
conduzindo inevitavelmente a mais ações inábeis no futuro. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Dessa forma os ensinamentos
Budistas acerca de karma, longe de serem uma relíquia esquisita do passado, são
um desafio direto a uma crença básica - e um defeito básico - na nossa cultura.
Somente quando abandonamos nossa obsessão em encontrar orgulho no nosso passado
tribal e podemos na verdade sentir orgulho dos motivos que estão por detrás das
nossas ações no presente, podemos dizer que a palavra <i>karma, </i>no seu
sentido Budista, recuperou toda sua bagagem. E quando abrimos a bagagem nos
damos conta de que ela nos trouxe um presente: um presente que damos a nós
mesmos, e uns aos outros quando deixamos de lado nossos mitos sobre quem somos
e podemos ao invés disso sermos honestos acerca do que estamos fazendo com cada
momento - ao mesmo tempo que fazemos o esforço para agir da maneira correta. <o:p></o:p></span></p>

