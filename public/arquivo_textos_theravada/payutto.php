<p class=Tit1 align=center style='text-align:center'>Venerável P. A. Payutto</Tit1>

<p class=Normal>O Venerável Prayudh Payutto nasceu no dia 12 de Janeiro de 1939 na Província de Suphanburi, na região central da Tailândia. Ele concluiu os seus estudos secundários com uma bolsa de estudos outorgada pelo Ministério da Educação. Aos 13 anos ele se tornou noviço e ainda como noviço completou os exames mais elevados em Pali, sendo que por essa realização ele foi laureado com a rara distinção de ser ordenado como monge sob o Patrocínio Real em 1961.<o:p></o:p></span></p>

<p class=Normal>Depois de obter a Graduação com Distinção em Estudos Budistas da Mahachulalongkorn Buddhist University e um Certificado Superior em Educação, respectivamente em 1962 e 1963, ele atuou como Vice Secretário Geral da Buddhist University. Durante esse período ele promoveu muitas reformas no currículo da universidade e no sistema administrativo, enfocando o crescente papel da ordem monástica na sociedade e no desenvolvimento comunitário. Ele foi nomeado como Abade do Wat Propirane em Bangcoc em 1972. Depois de atuar como Abade por quatro anos ele renunciou a essa posição e continuou apoiando as responsabilidades monásticas numa escala mais ampla, tanto na Tailândia como em outros países. Recentemente ele estabeleceu um outro monastério, Wat Nyanavesakawan, nos arredores de Bangcoc.<o:p></o:p></span></p>

<p class=Normal>Em quarenta anos, desde quando foi ordenado como monge até o presente momento, o Venerável Payutto tem se dedicado à disseminação do Budismo, mostrando como os membros individuais da sociedade podem desenvolver a paz e a felicidade de forma inteligente – vendo as coisas como elas na verdade são e solucionando os problemas de forma racional – o que por sua vez é o caminho para paz e harmonia na comunidade. O seu trabalho produziu uma vasta quantidade de ensinamentos, palestras, discussões, sermões e livros, tanto na Tailândia como em outros países.<o:p></o:p></span></p>

<p class=Normal>Ele escreveu mais de 150 livros tratando de temas Budistas e sociais (a maioria em Tailandês e alguns em Inglês). Os seus livros são bem conhecidos na Tailândia como obras de profunda erudição: bem pesquisados, com boas notas de referência e bem escritos. Mas o mais importante é que os seus trabalhos indicam o caminho para a vida harmoniosa na comunidade.<o:p></o:p></span></p>

<p class=Normal>No exterior, o Venerável Payutto foi convidado a ensinar em muitas universidades importantes como a University of Pennsylvania, Swarthmore College em Pennsylvania, Harvard University em Cambridge, Massachusetts. O Venerável Payutto também foi convidado a proferir palestras em várias conferências mundiais promovidas por organizações internacionais como a ‘International Conference of Higher Education and the Promotion of Peace’, o ‘Buddhist Knowledge Exchange Program in Honour of His Majesty the King of Thailand’, o ‘Sixth Asian Workshop on Child and Adolescent Development’ e o ‘1993 Parliament of the World’s Religions in Chicago’.<o:p></o:p></span></p>

