<p class=Normal>Um novo estudo fornece evidências convincentes de que tipos específicos de "práticas da atenção plena" trazem benefícios para pacientes com certos tipos de problemas de saúde física e mental. </p>

<p class=Normal>O Dr. William R. Marchand do E. George Wahlen Veterans Affairs Medical Center e da Universidade de Utah, em Salt Lake City revisou estudos publicados e avaliou os benefícios para a saúde das práticas baseadas na atenção plena. </p>

<p class=Normal>"Uma revisão extensa das terapias que incluem a meditação como um componente-chave - conhecida como práticas baseadas na atenção plena - mostra evidências convincentes de que essas intervenções são eficazes no tratamento dos sintomas psiquiátricos e da dor, quando usadas em combinação com terapias mais convencionais", diz Marchand. </p>

<p class=Normal>Seu estudo foi publicado no Journal of Psychiatric Practice em Julho de 2012. </p>

<p class=Normal>Como parte da pesquisa, Marchand revisou estudos publicados que avaliam os benefícios para a saúde das práticas baseadas na atenção plena. </p>

<p class=Normal>A prática da atenção plena tem sido descrita como "a prática de aprender a focar a atenção em cada momento da experiência com uma atitude de curiosidade, aceitação e abertura".</p>

<p class=Normal>Especialistas costumam dizer que "praticar a atenção plena é simplesmente viver o momento presente, sem tentar mudar nada." </p>

<p class=Normal>Os pesquisadores avaliaram três técnicas populares: </p>

  <p class=Normal style='margin-left:1.0in'>  A meditação Zen, uma prática espiritual budista que envolve a prática da atenção plena por meio da meditação, geralmente com foco na atenção aos padrões respiratórios. </p>

  <p class=Normal style='margin-left:1.0in'>  Mindfulness-based stress reduction (MBSR), redução do estresse baseado na atenção plena, um método secular que emprega a meditação da atenção plena Budista combinada com elementos da yoga e educação sobre estresse e estratégias para sua superação. </p>

  <p class=Normal style='margin-left:1.0in'>  Mindfulness-Based cognitive therapy (MBCT), terapia cognitiva baseada na atenção plena, que combina MBSR com os princípios da terapia cognitiva (por exemplo, reconhecer e desligar-se de pensamentos negativos) para evitar uma recaída da depressão. </p>

<p class=Normal>Os investigadores descobriram evidências de que MBSR e MBCT podem ajudar a aliviar a angústia psicológica em geral e atenuar a depressão e a ansiedade. Especificamente, uma  avaliação baseada em evidências "recomenda fortemente" MBCT como um complemento aos tratamentos convencionais (tratamento complementário) para a depressão unipolar. </p>

<p class=Normal>Os pesquisadores descobriram também que tanto MBSR e MBCT são eficazes tratamentos complementários para a ansiedade. </p>

<p class=Normal>Os investigadores dizem que os resultados também suportam a eficácia de MBSR para ajudar a reduzir o estresse e promover a saúde psicológica geral em pacientes com várias doenças médicas ou psiquiátricas. </p>

<p class=Normal>Por si só, MBSR foi útil no gerenciamento do estresse e na promoção da saúde psicológica em geral em pessoas saudáveis. Há também evidências de que a meditação Zen e MBSR foram  tratamentos complementários úteis para o tratamento da dor. </p>

<p class=Normal>Embora os métodos precisos através dos quais as técnicas de atenção plena beneficiam a saúde física e mental são desconhecidos, os pesquisadores acreditam que os mapeamentos do cérebro e tecnologias emergentes irão fornecer evidências no futuro próximo. </p>

<p class=Normal>"Essas práticas de atenção plena mostram promessa considerável e as evidências disponíveis indicam que o seu uso é justificado em uma variedade de situações clínicas", diz Marchand. </p>

<p class=Normal>Embora atualmente haja pouca evidência de quais tipos de pacientes estariam mais propensos a se beneficiar da técnica, Marchand sugere que as preferências do paciente e o seu entusiasmo são um bom guia. </p>

<p class=Normal>Ele comenta: "As considerações mais importantes podem ser o desejo de experimentar uma prática baseada na atenção plena e a disposição de se envolver na prática regular da meditação sentada." </p>




