<p class=Normal>Agência France-Presse 6 de Abril de 2011

<p class=Normal>Washington, EUA - Um estudo divulgado recentemente revela que um principiante com apenas 80 minutos de treinamento na prática de meditação pode produzir efeitos poderosos de redução da dor no cérebro. </p>

<p class=Normal>"Este é o primeiro estudo demonstrando que um pouco mais de uma hora de prática de meditação pode reduzir de forma dramática a experiência da dor e a ativação das áreas do cérebro relacionadas com a dor", afirmou Fadel Zeidan, autor do estudo e membro da equipe de pesquisas de pós-doutorado do Wake Forest Baptist Medical Center na Carolina do Norte. Ele ainda explicou que "Este estudo demonstra que a meditação produz efeitos mensuráveis no cérebro e pode proporcionar uma maneira eficaz das pessoas reduzirem a sensação de dor sem recorrer a medicamentos." </p>

<p class=Normal>O resultado do estudo foi publicado na edição de 6 de Abril do Journal of Neuroscience. </p>

<p class=Normal>"Encontramos um efeito significativo - cerca de 40% de redução na intensidade da dor e 57% de redução na sensação desagradável causada pela dor. A meditação produz uma redução mais significativa na dor do que mesmo a morfina ou outros medicamentos contra a dor, que em geral reduzem a intensidade da dor por volta de 25%", ele acrescentou. </p>

<p class=Normal>Os pesquisadores trabalharam com um grupo de 15 voluntários com boa saúde que nunca tinham meditado. Os voluntários foram treinados durante 20 minutos por dia, ao longo de 4 dias, para aprender a meditar trabalhando com a respiração e deixando de lado as emoções e os pensamentos. </p>

<p class=Normal>Antes e depois da meditação a atividade cerebral dos participantes foi monitorada com um tipo especial de imagem por ressonância magnética (MRI).</p>

<p class=Normal>Denominada "arterial spin labeling magnetic resonance imaging" (ASL MRI), esse exame é capaz de produzir uma melhor leitura de processos cerebrais com duração mais longa, tal como a meditação, do que um exame MRI tradicional do cérebro. </p>

<p class=Normal>Enquanto estavam sendo submetidos ao exame ASL MRI um dispositivo produzindo calor foi colocado na coxa direita dos participantes. Esse dispositivo aqueceu durante 5 minutos uma pequena área da pele a uma temperatura de 120oF (49oC), o que para a maioria das pessoas provocaria uma reação de dor. </p>

<p class=Normal>O exame feito depois da meditação mostrou que a intensidade da dor sentida por todos participantes foi reduzida, com diminuições entre 11% e 93%, afirmou Zeidan. </p>

<p class=Normal>Por outro lado, a meditação também reduziu a atividade somatosensorial no córtex primário, uma área que está envolvida na criação da sensação de localização e intensidade de um determinado estímulo sensorial de dor. </p>

<p class=Normal>O exame feito antes do treinamento na prática da meditação mostrou que a atividade nesta área era bastante alta; mas ao fazerem o exame durante a meditação, a atividade nesta região, que é muito importante no processamento da dor, não foi detectada, é como se essa área tivesse sido desligada. </p>

<p class=Normal>As áreas do cérebro responsáveis pela manutenção do foco e pelo processamento das emoções também estiveram mais ativas durante a meditação e a atividade foi mais intensa entre os participantes que relataram a maior redução na sensação de dor. "Uma das razões porque a meditação pode ter sido tão eficaz em bloquear a dor é que a sua atuação ocorre não somente numa área do cérebro, mas ao invés disso reduz a dor em múltiplos níveis de processamento," completou Zeidan. </p>

<p class=Normal>Em geral é entendido que a meditação alivia a dor não através da diminuição da sensação mas ajudando as pessoas a controlar de modo consciente a percepção da dor, diz Katharine MacLean, Ph.D., uma pesquisadora dos efeitos da meditação na Johns Hopkins University, em Baltimore. </p>

<p class=Normal>No entanto, ela diz, o exame do cérebro deixa claro que ambos processos estão ocorrendo: a meditação muda a natureza da dor antes desta ser percebida e também possibilita que as pessoas saibam melhor como lidar com a dor. "A meditação é na verdade um tipo de afinação do cérebro," diz MacLean. </p>

<p class=Normal>A técnica de meditação empregada no estudo é denominada "atenção plena (mindfulness)". Empregando a respiração como âncora o meditador observa, sem fazer julgamentos, tudo que estiver ocorrendo no corpo e na mente. </p> 


