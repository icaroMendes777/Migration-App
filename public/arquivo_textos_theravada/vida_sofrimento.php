<p class=Normal>&quot;Ele
me mostrou a luminosidade do mundo.&quot; <o:p></o:p></span></p>

<p class=Normal>Assim
foi como meu mestre, Ajaan Fuang, certa vez descreveu o seu débito para com o
seu mestre, Ajaan Lee. As suas palavras me pegaram de surpresa. Eu havia
começado a estudar com ele fazia pouco tempo, recém saído da escola onde havia
aprendido que os Budistas sérios adotavam uma visão negativa, pessimista do
mundo. No entanto, aqui se encontrava um homem que havia entregado a sua vida à
prática dos ensinamentos do Buda, falando da luminosidade do mundo. É claro que
por &quot;luminosidade&quot; ele não estava se referindo aos prazeres das
artes, comida, viagens, esportes, vida em família ou qualquer uma das demais
seções do jornal de Domingo. Ele se referia a uma felicidade mais profunda que
vem de dentro. À medida que passei a conhecê-lo melhor, obtive a percepção de
quão profundamente feliz ele era. Ele podia ser cético em relação a muitas das
aparências humanas, mas eu nunca o descreveria como sendo negativo ou
pessimista. &quot;Realista&quot; estaria mais próximo da verdade. No entanto,
durante muito tempo eu não consegui me livrar do paradoxo que sentia de como o
pessimismo dos textos Budistas podiam se materializar em uma pessoa tão feliz. <o:p></o:p></span></p>

<p class=Normal>Somente
quando comecei a ler os textos mais antigos foi que me dei conta de que aquilo
que eu pensava ser um paradoxo era na verdade uma ironia - a ironia de como o
Budismo, que proporciona uma visão tão positiva do potencial humano para
encontrar a verdadeira felicidade, pôde ser rotulado no Ocidente como negativo
e pessimista. <o:p></o:p></span></p>

<p class=Normal>Você
provavelmente ouviu o rumor de que &quot;a vida é sofrimento&quot; como sendo o
primeiro princípio do Budismo, a primeira nobre verdade do Buda. É um rumor com
boas credenciais, disseminado por acadêmicos de respeito e mestres do Dhamma,
mas apesar disso é um rumor. A verdade sobre as nobres verdades é muito mais
interessante. O Buda ensinou quatro verdades - não uma - a respeito da vida:
existe o sofrimento, existe uma causa para o sofrimento, existe um fim para o
sofrimento e existe um caminho de prática que dá um fim ao sofrimento. Essas
verdades, tomadas em conjunto, estão muito longe de serem pessimistas. Elas são
uma abordagem prática para solucionar um problema - a maneira como um médico
encara uma enfermidade ou um mecânico, um motor defeituoso. Você identifica um
problema e investiga as suas causas. Depois então você dá um fim ao problema
eliminando as suas causas. <o:p></o:p></span></p>

<p class=Normal>O
que é especial na abordagem do Buda é que o problema que ele ataca é o
sofrimento humano na sua totalidade, e a solução que ele oferece é algo que os
seres humanos podem fazer por si mesmos. Da mesma forma como um médico que
conhece a cura infalível para o sarampo não teme o sarampo, o Buda não teme
nenhum aspecto do sofrimento humano. E, tendo experimentado a felicidade que é
completamente não condicionada, ele não teme apontar o sofrimento e estresse
inerentes em lugares que a maioria de nós não consegue ver - nos prazeres
condicionados aos quais nos apegamos. Ele nos ensinou que não devemos negar
esse sofrimento e estresse ou tentar fugir deles, mas, ficar tranqüilos e
enfrentá-los. Examiná-los com cuidado. Dessa forma - entendo-os - podemos
desenraizar as suas causas e dar um fim neles. Totalmente. Quão confiante você
se sente? <o:p></o:p></span></p>

<p class=Normal>Um
razoável número de escritores vêm apontando a confiabilidade básica das quatro
nobres verdades, mas, apesar disso, o rumor do pessimismo Budista persiste. Eu
me pergunto porque. Uma explicação possível é que, ao ir de encontro ao
Budismo, nós temos no subconsciente a esperança de que ele venha a trazer uma solução
para temas que possuem uma longa história na nossa cultura. Ao iniciar com o
sofrimento como a sua primeira verdade, o Buda parece estar oferecendo a sua
opinião acerca de uma questão que possui uma longa história no Ocidente: o
mundo é fundamentalmente bom ou mal? <o:p></o:p></span></p>

<p class=Normal>De
acordo com o Gênesis, a primeira questão que ocorreu a Deus após ele haver
terminado a sua criação foi: ele havia feito um bom trabalho? Então ele olhou
para o mundo e viu que ele era bom. Desde então, as pessoas no Ocidente têm
ficado do lado ou contra Deus nessa sua resposta, mas agindo assim elas têm
afirmado que, em primeiro lugar, a questão vale a pena ser perguntada. Quando o
Theravada - a única forma do Budismo que enfrentou o Cristianismo quando a
Europa colonizou a Ásia - estava buscando formas de enfrentar aquilo que era
visto como a ameaça missionária, os Budistas que haviam sido educados pelos
missionários assumiram que a pergunta era válida e utilizaram a primeira nobre
verdade como uma refutação ao Deus Cristão: vejam como a vida é miserável, eles
diziam, é difícil aceitar o veredicto Divino acerca da sua obra. <o:p></o:p></span></p>

<p class=Normal>Essa
estratégia de debate pode ter sido favorável naquela época e ainda é fácil
encontrar apologistas Budistas que - ainda vivendo no passado colonial –
continuam utilizando os mesmos argumentos. No entanto, a verdadeira questão é,
primeiramente, se o Buda pretendeu que a primeira nobre verdade fosse uma
resposta à pergunta de Deus e – mais importante - se estamos obtendo o máximo
da primeira nobre verdade encarando-a sob essa perspectiva. <o:p></o:p></span></p>

<p class=Normal>É difícil imaginar o que você poderia alcançar dizendo
que a vida é sofrimento. Você gastaria o seu tempo argumentando com as pessoas
que enxergam mais do que apenas sofrimento na vida. O próprio Buda diz o mesmo
em um dos seus discursos. Um brâmane chamado Dighanakha
o procura e anuncia que ele não concorda com nada. Essa seria uma perfeita
ocasião para o Buda, se ele quisesse, interceder pela verdade de que a vida é
sofrimento. Ao invés disso, ele ataca essa noção de tomar uma posição sobre se
a vida merece aprovação ou não. Existem três respostas possíveis para essa
questão: (1) nada merece aprovação, (2) tudo merece e (3) algumas coisas
merecem e outras não. Se você tomar qualquer uma dessas três posições, você acabará
discutindo com as pessoas que tomam qualquer uma das outras duas posições. E o
que você ganha com isso?<o:p></o:p></span></p>

<p class=Normal>O
Buda, então, instrui Dighanakha a olhar para o seu corpo e sensações como
ilustrações da primeira nobre verdade: eles são estressantes, inconstantes e
não merecem que nos apeguemos a eles como sendo o eu. Dighanakha segue as
instruções do Buda e ao soltar-se do seu apego ao corpo e às sensações,
conquista o seu primeiro vislumbre do Imortal que significa estar totalmente
livre do sofrimento. <o:p></o:p></span></p>

<p class=Normal>A
mensagem desta estória é que tentar responder à pergunta de Deus, fazendo um
julgamento do mundo, é uma perda de tempo. E ela oferece uma melhor utilização
para a primeira nobre verdade: olhando para as coisas não em termos de
&quot;mundo&quot; ou &quot;vida&quot;, mas simplesmente apontando o sofrimento
para que você possa compreendê-lo, soltar-se dele e obter a libertação. Ao
invés de querer que façamos um julgamento que englobe tudo - o que, na prática,
significa pedir que sejamos partidários cegos - a primeira nobre verdade nos
pede para olhar e ver com precisão onde se encontra o problema do sofrimento. <o:p></o:p></span></p>

<p class=Normal> Outros discursos enfatizam que o problema não está no
corpo e nas sensações em si mesmos. Eles mesmos não são o sofrimento. O
sofrimento está no apego a eles. Na definição da primeira nobre verdade o Buda
resume todos os tipos de sofrimento na frase, &quot;os cinco agregados influenciados
pelo apego&quot;: apego à forma física (incluindo o corpo), sensações,
percepções, formações mentais e consciência. No entanto, ele nos diz que quando
os cinco agregados estão livres de apego, eles conduzem ao benefício e
felicidade por um longo período. <o:p></o:p></span></p>

<p class=Normal>Portanto,
em resumo, a primeira nobre verdade quer dizer que o <i>apego </i>é sofrimento.
É devido ao apego que a dor física se torna uma dor mental. É devido ao apego
que o envelhecimento, enfermidade e morte causam angústia mental. Como nos
apegamos? Os textos indicam quatro formas: o apego ao desejo sensual, o apego a
idéias, o apego a preceitos e rituais e o apego à ideia da existência de um eu.
É raro que na mente comum exista algum momento sem alguma forma de apego. Mesmo
quando abandonamos uma forma particular de apego é porque em geral ela está
atrapalhando alguma outra forma. Podemos abandonar uma idéia puritana porque
ela interfere com o prazer sensual; ou um prazer sensual porque ele conflita
com uma idéia acerca do que devemos fazer para ter boa saúde. A nossa idéia
sobre quem somos pode se expandir e contrair dependendo de qual das nossas
muitas noções de &quot;eu&quot; está sentindo mais dor: ela pode se expandir
para uma noção cósmica de unidade com todos os seres, quando nos sentimos
confinados pelas limitações do nosso pequeno complexo de mente e corpo; ela
pode contrair-se numa pequena concha quando sentimos a dor que surge da
identificação com um cosmo repleto de crueldade, negligência e estupidez. E
então chegamos ao ponto em que a insignificância do nosso eu finito se torna
novamente opressiva. <o:p></o:p></span></p>

<p class=Normal>Portanto,
descobrimos que a nossa mente salta de apego em apego tal como uma semente de
mostarda em uma frigideira quente. Quando nos damos conta disso, naturalmente
procuramos uma forma de escapar. E é nesse ponto que se torna tão importante
que a primeira nobre verdade <i>não </i>diga que &quot;a vida é
sofrimento&quot;, pois, se a vida fosse sofrimento, onde poderíamos encontrar
um fim para o sofrimento? Não nos restaria nada além de morte e aniquilação.
Mas, quando na verdade o apego é o sofrimento, nós simplesmente temos que olhar
para ver com clareza onde está o apego e aprender a não nos apegarmos.<o:p></o:p></span></p>

<p class=Normal>É
nesse ponto que nos deparamos com a grande habilidade do Buda como
estrategista: Ele nos diz que tomemos os apegos que devemos abandonar e que os
transformemos no caminho para o seu abandono. Necessitaremos de uma certa dose
de prazer sensual - em termos de comida, roupas e moradia adequadas - para encontrarmos
a força para superar o prazer sensual. Necessitaremos do Entendimento Correto
para superar o apego a idéias, e das regras dos cinco preceitos éticos e da prática da meditação para
superar o apego a preceitos e rituais. Sustentando tudo isso, precisaremos de uma
forte noção de auto-responsabilidade para superar o apego à ideia da existência
de um eu. <o:p></o:p></span></p>

<p class=Normal>Assim,
nós começamos a trilhar o caminho para dar fim ao sofrimento sem tentar derrubar nossos apegos
imediatamente, mas aprendendo a nos apegar mais estrategicamente. Em
outras palavras, começamos do ponto onde nos encontramos e utilizamos da melhor
maneira possível os hábitos que já temos. Progredimos ao longo do caminho à
medida que encontramos mais e mais coisas de melhor qualidade às quais possamos
nos apegar e maneiras mais hábeis de nos apegarmos; da mesma maneira que você
usa uma escada para subir no telhado: agarra um degrau mais alto para poder
soltar o degrau mais baixo e depois agarra um outro degrau ainda mais alto. À
medida que os degraus se distanciam do chão, você perceberá que a mente ficará
cada vez mais clara e poderá ver com precisão onde estão os seus apegos. Ela
obterá uma noção mais evidente sobre qual parte daquilo que é experimentado
pertence a qual nobre verdade e o que deve ser feito em relação a ela: as
partes que são sofrimento devem ser compreendidas; as partes que causam o
sofrimento - desejo e ignorância - devem ser abandonadas; as partes que
constituem o caminho para o fim do sofrimento devem ser desenvolvidas; e as
partes que pertencem ao fim do sofrimento devem ser realizadas. Isso ajudará
você a subir cada vez mais alto na escada, até chegar ao telhado com segurança.
E nesse momento, você poderá finalmente se soltar da escada e libertar-se
completamente. <o:p></o:p></span></p>

<p class=Normal> Potanto,
a verdadeira questão que enfrentamos não é a pergunta de Deus, julgando a sua
habilidade em criar a vida no mundo. É a <i>nossa </i>questão: que tão
habilidosos somos em lidar com as vicissitudes da vida? Estamos nos apegando de
uma forma que apenas serve para dar continuidade ao ciclo de sofrimento ou
estamos aprendendo a nos apegar de maneira a reduzir o sofrimento para que, no
final das contas, possamos nos emancipar e não precisemos mais nos apegar . Se
lidarmos com a vida armados com as quatro nobres verdades, compreendendo que a
vida contém ambos, o sofrimento e o fim do sofrimento, haverá esperança:
esperança de que seremos capazes de identificar quais partes da vida pertencem
a qual verdade; esperança de que algum dia, nesta vida, descobriremos a
luminosidade ao ponto de podermos concordar com o Buda, &quot;Ah. Sim. Isto é o
fim do sofrimento e estresse&quot;. <o:p></o:p></span></p>


