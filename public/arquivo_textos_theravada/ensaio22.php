<p class=Normal>A prática dos ensinamentos do Buda é mais comumente descrita pela imagem de
uma jornada e os oito fatores do Nobre Caminho Óctuplo constituindo a estrada
real pela qual o discípulo deve viajar. As escrituras Budistas, no entanto,
ilustram a busca pela libertação de várias outras formas e cada uma delas
projetando uma luz distinta sobre a natureza da prática. Embora as formulações
alternativas inevitavelmente se apóiem sobre o mesmo conjunto básico de fatores
mentais como aqueles que compõem o caminho óctuplo, elas estruturam esses
fatores ao redor de uma &quot;metáfora raiz&quot; distinta - uma imagem que
evoca um conjunto particular de associações e que destaca diferentes aspectos
do esforço para alcançar a cessação do sofrimento. </p>

<p class=Normal>Um dos grupos de fatores, que recebe uma proeminência especial nos Suttas,
incluído pelo Buda entre os trinta e sete apoios para a iluminação, é o das
cinco faculdades espirituais: as faculdades da convicção, energia, atenção
plena, concentração e sabedoria. O termo <i>indriya, </i>faculdades, aplicado a
esse grupo como um todo é derivado do nome do antigo deus Indra dos Vedas,
soberano dos devas e o termo conseqüentemente sugere a qualidade divina de
controle e domínio. As cinco faculdades são designadas dessa forma porque elas
exercem controle nos seus compartimentos específicos da vida espiritual. Tal
como o deus Indra venceu os demônios e obteve a supremacia entre os devas, da
mesma forma cada uma das cinco faculdades é requisitada para subjugar uma
deficiência mental em particular e para dirigir a potência mental
correspondente em direção ao rompimento das barreiras para a alcançar a
iluminação. </p>

<p class=Normal>O conceito das faculdades é de certa forma semelhante ao antigo conceito
Grego das virtudes. Tal como as virtudes, as faculdades são poderes ativos que
coordenam e canalizam as nossas energias naturais, dirigindo-as para alcançar a
harmonia e o equilíbrio internos essenciais para a nossa verdadeira felicidade
e paz. Visto que as faculdades devem servir como agentes de controle interno,
isto implica que, exceto por sua influência de contenção, nossa natureza não se
encontra sob nosso controle. Deixada por sua própria conta, sem o governo de
uma fonte superior de orientação, a mente é vítima de forças que crescem dentro
de si mesma, forças tenebrosas que nos mantêm subjugados e que evitam que
alcancemos o nosso mais elevado bem estar e verdadeira bondade. Essas forças
são as contaminações <i>(kilesa). </i>Enquanto vivermos e agirmos sob o seu
domínio não seremos senhores de nós mesmos, mas apenas peões passivos,
compelidos pelos nossos desejos cegos a agir de formas que prometem satisfação
mas que no final conduzem apenas à miséria e escravidão. A verdadeira
felicidade envolve necessariamente alcançar a autonomia interna, a força para
resistir aos puxões e empurrões dos nossos apetites, e isso é alcançado
precisamente através do desenvolvimento das cinco faculdades espirituais. </p>

<p class=Normal>As qualidades que exercem a função de faculdades possuem origem humilde,
aparecendo inicialmente em papéis mundanos ao longo da nossa vida diária.
Nessas formas humildes elas se manifestam como a convicção honesta em valores
superiores, como o vigoroso esforço em direção ao bem, como a consciência
atenta, como a concentração focada e como a compreensão inteligente. O
ensinamento do Buda não implanta essas inclinações na mente a partir do nada,
mas explora essas capacidades preexistentes na nossa natureza dirigindo-as a um
objetivo supramundano - na direção da realização do Incondicionado –
atribuindo-lhes, dessa forma, um significado transcendente. Ao atribuir-lhes
uma tarefa que revela o seu imenso potencial e ao guiá-las por um caminho que
pode permitir a realização desse potencial, o Dhamma transforma esses fatores
mentais comuns em faculdades espirituais, instrumentos poderosos na busca pela
libertação, que são capazes de penetrar as mais profundas leis da existência e
abrir as portas para o Imortal. </p>

<p class=Normal>Na prática do Dhamma cada uma dessas faculdades tem que simultaneamente
desempenhar a sua própria função específica e se harmonizar com as demais
faculdades para estabelecer o equilíbrio necessário para a clara compreensão.
As cinco faculdades alcançam a completa maturidade no desenvolvimento do
insight através da meditação, o caminho direto para o despertar. Nesse
processo, a faculdade da convicção proporciona o elemento de inspiração e
aspiração que dirige a mente para longe do pântano da dúvida estabelecendo-a
com tranqüila convicção na Jóia Tríplice como a base suprema para a libertação.
A faculdade da energia acende o fogo do esforço sustentado que queima os
obstáculos e causa a maturação dos fatores que amadurecem com o despertar. A
faculdade da atenção plena contribui com a clara consciência, o antídoto para o
descuido e o pré-requisito para o entendimento. A faculdade da concentração
mantém o foco da atenção, calmo e controlado, firmemente focado na origem e cessação
dos eventos corporais e mentais. E a faculdade da sabedoria, que o Buda chama
de virtude máxima entre os apoios para a iluminação, remove a penumbra da
ignorância e ilumina as verdadeiras características dos fenômenos. </p>

<p class=Normal>Da mesma forma que as cinco faculdades, consideradas individualmente,
desempenham, cada uma, as suas tarefas únicas em seus respectivos domínios,
como um grupo elas executam a tarefa coletiva de estabelecer o equilíbrio
interno e a harmonia. Para obter esse esforço equilibrado, as faculdades são
divididas em pares nos quais cada membro deve contrapor a inerente tendência
indesejável do outro, permitindo assim que realizem o seu pleno potencial. As
faculdades da convicção e da sabedoria formam um par, tendo como objetivo
equilibrar a capacidade para a fé e a compreensão; as faculdades da energia e
da concentração formam um segundo par tendo como objetivo equilibrar a
capacidade para o esforço ativo e a calma contemplação. Acima dos pares
complementares encontra-se a faculdade da atenção plena que protege a mente dos
extremos e assegura que os membros de cada par mantenham um ao outro em um
refreamento mútuo, uma tensão que os enriquece mutuamente. </p>

<p class=Normal>Nascidas de origens humildes nas funções do dia a dia da mente, através do
Dhamma as cinco faculdades adquirem um destino transcendente. Quando elas são
desenvolvidas e cultivadas com regularidade, diz o Mestre, &quot;elas conduzem
ao Imortal, têm como destino o Imortal, culminam no Imortal&quot;. </p>



