<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O Buda
aconselhava os bhikkhus, “Bhikkhus, quando vocês estiverem reunidos deveriam
fazer uma de duas coisas: discutir o Dhamma ou observar o nobre silêncio.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O nobre silêncio é o estado
da mente em que não há pensamentos. A mente fica completamente em silêncio. Os
pensamentos só podem ser silenciados se treinarmos a nossa mente através da
prática correta da meditação.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O meditador deve começar
dando atenção total e ininterrupta a um único objeto sem verbalizar a
experiência na mente. Ao verbalizar e conceituar as coisas, por um lado, a
atenção é interrompida, e por outro, os pensamentos são perpetuados.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ao verbalizar, você
adiciona cada vez mais idéias ou conceitos. A realidade não é uma palavra ou um
verbo. A realidade é aquilo que você experimenta. Ao experimentar dores e
desconfortos ou prazer e felicidade durante a meditação, você nota a
experiência diretamente tal como ela é. Você não precisa estabelecer uma ponte
conceitual entre a experiência e o conhecimento direto. Ao sentir fome, você
experimenta a fome sem ter que dizer: “Estou com fome, estou com fome.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os substantivos e verbos
são necessários apenas para comunicar a sua experiência. Ao meditar você
observa total silêncio, sem falar com ninguém sobre o que você está
experimentando. Você deve conhecer a si mesmo, exatamente como você é. Você
deve sentir-se exatamente como você é.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Desde quando somos bebês
até a universidade aprendemos a usar palavras, conceitos e idéias para fazer
com que os outros nos compreendam. Mas durante a meditação você não está
tentando comunicar a sua experiência para ninguém. Ao treinar a sua mente para
que ela permaneça em silêncio, você fará com que ela silencie. Se você
adicionar mais palavras à mente, ela, simplesmente, se manterá ocupada.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Todos já tivemos a
oportunidade de observar pessoas sentadas ou andando pelas ruas conversando
consigo mesmas. Elas são incapazes de silenciar a mente. Esse é um exemplo
extremo da incapacidade de silenciar os pensamentos. Mas cada um de nós, do seu
próprio jeito, nos debatemos com isso na vida diária e na meditação. No fundo é
isso; a não ser que você tente, nunca será capaz de fazer com que os
pensamentos cessem. Você só silencia os pensamentos quando tem a determinação
de fazê-lo.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Preste total atenção àquilo
que você experimenta através dos seis sentidos sem rotular aquilo que surgir.
Existem certas coisas que você experimenta para as quais não são necessárias
palavras. Você simplesmente as conhece. A sua mente as conhece. Você permanece
com esse conhecimento. Ao sentir frio, a reação habitual é dizer para si mesmo,
“Nossa, que frio.” Ao sentir calor, você pensa automaticamente, “Nossa, que
calor.” Simplesmente, preste atenção ao frio que você estiver sentindo sem esse
pensamento adicional. Sinta o calor sem verbalizar a experiência. Ao lembrar um
lugar que você visitou, ou alguém com quem você conversou, ou um sorvete que
você comeu, ou a mão de alguém que você segurou, tenha consciência desses
objetos na sua memória, tão somente.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Para silenciar os
pensamentos é necessário estar completamente concentrado. Você consegue isso
prestando total atenção a um objeto de cada vez. Se você iniciar a prática
focando a mente exclusivamente num único objeto, gradualmente você irá
condicionar a mente a superar os pensamentos discursivos sustentando o contato
inicial com o objeto.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ao ouvir as batidas do seu
coração você não necessita de conceitos para sentir essa sutil ocorrência. Da
mesma forma, durante a meditação ao prestar total atenção à inspiração e à
expiração, poderá notar o início, o meio e o fim de cada inalação e exalação.
Você poderá notar a breve pausa entre cada inalação e exalação. Essas
ocorrências naturais na sua respiração você poderá notar se estiver plenamente
atento a elas.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A mente se move com tamanha
rapidez, e no entanto podemos treiná-la a notar esses eventos de forma
precisa<span style="mso-spacerun: yes">  </span>à medida em que eles ocorrem,
já que eles ocorrem de forma consecutiva. Se você conceituar essas ocorrências
então será incapaz de notá-las. Ao invés disso, ficará perdido nas palavras e
não se dará conta da experiência real. Você não precisa dizer, “Este é o início
da inspiração,” ou “Este é o meio” ou “Este é o fim.” Simplesmente observe
esses estágios. Você não precisa do pensamento para notá-los. Tudo que você
precisa é de atenção.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>De forma alguma nos
convertemos num vegetal ao silenciar os pensamentos. Uma mente silenciosa é
receptiva ao insight. E você poderá silenciar o processo dos pensamentos
através do treinamento sistemático da mente.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Eu uso a frase “acalmar a
mente” ou “silenciar a mente” querendo dizer não ter pensamentos na mente, mas
desacelerar a mente não é como a desaceleração do metabolismo do corpo durante
a hibernação. Significa, simplesmente, não ter o hábito de criar pensamentos na
mente.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A mente não produz
pensamentos se não for estimulada pela verbalização habitual. Só quando nos
treinarmos numa prática constante no sentido de parar a verbalização, a mente
poderá experimentar as coisas como elas na verdade são. Silenciando a mente, poderemos
experimentar a verdadeira paz. Enquanto vários tipos de pensamentos agitarem a
mente, não poderemos experimentar cem por cento de paz.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A paz não é um pensamento,
não é um conceito, é uma experiência não verbal. Uma pessoa pode permanecer
nesse estado de paz por até sete dias. Mas para alcançar esse estado de
completa paz mental, é necessário um treinamento gradual para desacelerar os
pensamentos. Uma vez desacelerados, a mente passa a não se alimentar de novos
pensamentos.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Mesmo quando não estamos
meditando, experimentamos muitas coisas de forma profunda para as quais com
freqüência não existem palavras. Podemos tentar encontrar uma palavra ou um
verbo para aquela experiência. Podemos chamá-la de intuição. No entanto, as
intuições podem surgir sem ter palavras ou conceitos associados a elas. Você
também pode ouvir sons sem que palavras surjam na mente. Diz-se que a melhor
forma de desfrutar da música é ouvir música. Ao ouvir música, você ouve os sons
sem tentar verbalizá-los. Pense em como você ouve o gorjeio de um pássaro; você
não verbaliza o som. Você poderia dizer “O canário gorjeia assim...” mas isso é
a sua imaginação.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso significa que mesmo
fora da meditação você poderá experimentar muitas coisas bastante sutis,
simplesmente, prestando total atenção aos seus sentidos. Na maior parte do
tempo, verbalizamos as coisas após tê-las experimentado, não enquanto as
experimentamos. Mas quando você presta total atenção a algo, sem
verbalização,<span style="mso-spacerun: yes">  </span>você obtém a concentração
que não pode ser alcançada com a verbalização. As palavras estimulam a mente.
Por isso, a mente permanece produzindo mais e mais palavras que serão
expressadas em pensamentos. Através da atenção não verbal, você poderá
minimizar o número de palavras utilizadas de maneira regular. Quando as
palavras são minimizadas, os pensamentos são minimizados. Por fim, esse
processo faz com que a mente fique verdadeiramente livre de pensamentos. Mas,
se você não minimizar as palavras, não poderá libertar a mente dos pensamentos.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ao experimentar algo, se
você não tentar traduzir a experiência em palavras, você, simplesmente, terá a
experiência, não os pensamentos. Visões, sons, odores, sabores, toques podem
ser experimentados diretamente sem palavras. Ao usar palavras você bloqueia a
experiência direta dos objetos sensoriais.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>No final, não são as
palavras que fazem com que você experimente as suas experiências. Suponha que a
cor branca apareça na frente dos seus olhos. O branco reflete nos seus olhos. A
mente o identifica tal como ele é. Só se você quiser expressar aquilo que viu
para alguém é que necessitará de palavras. No entanto, a brancura é uma
experiência, é o que é, ela vai muito além dessa palavra. O negror também, é o
que é, e vai muito além dessa palavra. O mesmo se aplica à doçura, amargor, acidez,
dureza e tudo o mais que você venha a experimentar.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A mente não produz
pensamentos do nada. Ela tem que ser alimentada com algo que usará como matéria
prima para produzir os pensamentos. A matéria prima é aquilo que você forneceu
como alimento no passado. Se você não alimentá-la com palavras, se você
treiná-la evitando a verbalização, a mente não poderá produzir pensamentos do
vácuo.<o:p></o:p></span></p>
