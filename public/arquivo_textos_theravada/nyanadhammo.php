<p class=Normal>Ajaan Nyanadhammo nasceu em Adelaide, na Austrália, em 1955.</p> 

<p class=Normal>Ele encontrou inspiração nos ensinamentos do Buda enquanto estudava biologia na Universidade. </p>

<p class=Normal>Em 1978, viajou para a Tailândia para ser ordenado pelo Somdet Phra Nyanasamvara. Ele recebeu a ordenação completa com Ajaan Chah e depois passou muitos anos na selva praticando meditação e aprendendo o Dhamma de grandes mestres que viviam em monastérios isolados. </p>

<p class=Normal>De 1994 a 2002, esteve no Bodhinyana Monastery como vice-abade de Ajaan Brahm. </p>

<p class=Normal>De 2002 a 2007, foi o abade do Wat Pa Nanachat. </p>

<p class=Normal>Desde 2007, estabeleceu um novo monastério, Wat Ratanawan, cerca de duas horas de carro de Bangkok.</p>


