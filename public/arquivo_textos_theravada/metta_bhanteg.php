<p class=Normal>Algumas vezes a prática da meditação de insight <i style='mso-bidi-font-style:
normal'>(vipassana) </i>pode ser interpretada como sendo um tipo de prática que
faz com que o meditador se torne um ser sem coração ou indiferente, como um
vegetal que não possui amor e compaixão pelos outros seres vivos</p>

<p class=Normal>Devemos, no entanto, lembrar que o Buda recomendou de maneira enfática que
cultivemos quatro moradas divinas ou estados sublimes da mente: amor bondade <i
style='mso-bidi-font-style:normal'>(metta), </i>compaixão <i style='mso-bidi-font-style:
normal'>(karuna), </i>alegria altruísta <i style='mso-bidi-font-style:normal'>(mudita)
</i>e equanimidade <i style='mso-bidi-font-style:normal'>(upekha).</i> O
primeiro desses quatro é tão importante que o Buda disse que alguém que dependa
inteiramente de outras pessoas para viver (isto é, um monge ou monja) poderá
pagar a sua dívida para com os seus patrocinadores<span style="mso-spacerun:
yes">  </span>através da prática de <i style='mso-bidi-font-style:normal'>metta,
</i>dirigida a todos os seres vivos, mesmo que seja por um período de tempo tão
curto quanto uma fração de segundo por dia. O Karaniya Metta Sutta diz: &quot;…sempre
que estiver desperto, cultive essa atenção plena: a isto se denomina uma morada
divina no aqui e agora.&quot;<span style="mso-spacerun: yes">  </span></p>

<p class=Normal>A atenção plena<span lang=PT-BR style='mso-ansi-language:PT-BR'> é um dos
fatores mais importantes em todo o conjunto dos ensinamentos do Buda. Desde o
dia que ele alcançou a iluminação, até o dia em que ele faleceu aos 80 anos, em
quase todos os discursos do Dhamma ele enfatizou a atenção plena. Ao equiparar
a prática de <i style='mso-bidi-font-style:normal'>metta </i>à atenção plena
podemos entender a importância dessa prática dentro do conjunto dos
ensinamentos do Buda. O Buda aperfeiçoou a prática de <i style='mso-bidi-font-style:
normal'>metta </i>para alcançar a Iluminação, equilibrando-a com a sabedoria.
Mesmo após haver alcançado a Iluminação, a primeira coisa que ele fazia todos
os dias era penetrar a realização da Grande Compaixão, que é o fruto da prática
de <i style='mso-bidi-font-style:normal'>metta.</i> Em seguida ele inspecionava
o mundo para ver se havia seres que ele poderia auxiliar na compreensão do
Dhamma. Esses quatro estados sublimes da mente são denominados Brahma Vihara, o
comportamento ideal ou atitude ideal. Os três primeiros possuem força
suficiente para permitir alcançar os primeiros três jhanas e o último para
alcançar o quarto jhana. A sua importância na prática da meditação vipassana fica
evidente pelo fato deles estarem incluídos no segundo elemento do Nobre Caminho
Óctuplo. Na verdade, nenhuma concentração é possível sem esses estados sublimes
da mente, porque na ausência deles a mente fica cheia de raiva, rigidez,
preocupações, temores, tensão e inquietação.<span style="mso-spacerun: yes"> 
</span><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Antes de nos dedicarmos à
prática desses estados nobres da mente, devemos superar a raiva, que é uma
maneira impensada de desperdiçar a própria energia. A raiva, quando ativa, se
compara à água fervendo ou, quando não é expressa, ao preconceito. Ela pode
destruir a nossa prática de meditação e o treinamento das virtudes. A pessoa
enraivecida se compara a um pedaço de lenha meio queimado deixado em uma pira
funerária. Ambas as extremidades da lenha estão queimadas e transformadas em
carvão e o meio está coberto com imundícies. Ninguém pegaria esse pedaço de
lenha para queimar ou para qualquer outro propósito porque ficaria com as mão
imundas. Da mesma forma a pessoa enraivecida será evitada de todas as formas,
se possível, por todos. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Precisamos começar a
prática de <i style='mso-bidi-font-style:normal'>metta </i>com nós mesmos
primeiro. Algumas vezes alguns de vocês podem se perguntar porque devemos amar
a nós mesmos primeiro. Isso não seria amor próprio que conduz ao egoísmo?<span
style="mso-spacerun: yes">  </span>Se você investigar a sua mente cuidadosamente
no entanto, irá se convencer de que não existe nenhuma outra pessoa em todo o
universo que você ame mais do que você mesmo. O Buda disse, “Tendo atravessado
todos os quadrantes com a mente, ele não encontra em nenhum lugar alguém mais
querido do que ele mesmo. Do mesmo modo, toda pessoa considera a si mesma como
a mais querida; por conseguinte quem ama a si mesmo não deveria ferir os
outros.” <span style="mso-spacerun: yes"> </span>(<a href="../sutta/SNIII.8.php">Mallika
Sutta</a>). Quem não ama a si mesmo será incapaz de amar aos outros. Da mesma
forma, quem ama a si mesmo irá sentir o impacto de <i style='mso-bidi-font-style:
normal'>metta </i>e poderá então compreender quão belo seria se todos os
corações no mundo estivessem plenos com o mesmo sentimento de <i
style='mso-bidi-font-style:normal'>metta.<o:p></o:p></i></span></p>

<p class=Normal>O tipo de amor bondade que queremos cultivar não é o amor comum tal como é entendido no uso diário. Quando você diz, por exemplo, "eu amo tal pessoa" ou "tal coisa", o que você realmente está querendo dizer é que você deseja a aparência, comportamento, idéias, tom da voz ou a atitude em geral daquela pessoa, em relação a você particularmente, ou em relação à vida de forma geral. Se aquela pessoa mudar as coisas pelas quais você a deseja, você poderá decidir que não a ama mais. Se as preferências, caprichos e fantasias das pessoas mudarem, elas não mais dirão "eu amo tal pessoa". Nessa dualidade de amor-ódio você ama uma pessoa e odeia outra. Você ama agora e odeia depois. Você ama quando quiser e odeia quando quiser. Você ama quando tudo está bem e sem problemas e odeia quando alguma coisa dá errado no relacionamento entre você e a outra pessoa. Se o seu amor muda dessa forma de tempos em tempos, de lugar em lugar e de situação para situação, então o que você chama de "amor" não é metta mas sim desejo, cobiça ou luxúria - de nenhuma forma isso é amor.</p> 
<p class=Normal>O tipo de amor bondade que queremos cultivar através da meditação não possui um oposto ou um motivo velado. Assim, a dicotomia amor-ódio não se aplica ao amor bondade cultivado através da sabedoria ou da atenção plena, pois ele nunca irá se transformar em ódio à medida que as circunstâncias mudarem. O verdadeiro amor bondade é uma faculdade natural que está oculta sob o amontoado de desejo, raiva e ignorância. Ele não pode ser dado. Nós precisamos encontrá-lo dentro de nós mesmos e cultivá-lo com a atenção plena. A atenção plena o descobre, cultiva e mantém. A consciência do "eu" (<i>ahankara</i>) se dissolve com a atenção plena e o seu lugar é tomado pelo amor bondade isento de egoísmo.</p>
<p class=Normal>Por causa do nosso egoísmo odiamos algumas pessoas. Queremos viver de certo jeito, queremos fazer certas coisas ao nosso modo, perceber as coisas de uma forma particular; e não de outro jeito. Se outras pessoas não concordarem com as nossas opiniões, nossos jeitos e nossos estilos, não só  as odiaremos, mas também nos tornaremos tão cegos e irracionais, devido à falta de atenção plena, que poderemos chegar ao ponto de negar-lhes o direito à vida.</p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando você pratica <i
style='mso-bidi-font-style:normal'>metta </i>você não fica colérico por não
receber algum tipo de retribuição de pessoas e seres para os quais você
irradiou o seu <i style='mso-bidi-font-style:normal'>metta, </i>porque ao
irradiar-lhes o seu amor bondade você não possuía nenhum motivo oculto. Nessa
rede de amor bondade você não somente inclui todos os seres tal como eles são,
mas você deseja que todos eles, sem qualquer discriminação, sejam felizes. Você
irá se portar de maneira gentil e agradável para com todos e irá falar a respeito
deles de forma gentil e agradável tanto na presença como na ausência deles. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ao meditar as nossas mentes
e corpos se tornam naturalmente relaxados. Os obstáculos são dissolvidos. A
sonolência e o torpor por exemplo são substituídos pela vigilância. A dúvida é
substituída pela confiança, a raiva pela alegria, a inquietação e a preocupação
pela felicidade. Na medida que o ressentimento é substituído pela alegria, o
amor bondade escondido no nosso subconsciente se manifesta fazendo com que
fiquemos ainda mais em paz e felizes. Nesse estado de meditação obtemos
concentração e superamos a nossa cobiça. Podemos ver como a meditação destrói a
raiva e cultiva <i style='mso-bidi-font-style:normal'>metta</i>, que por seu
lado dá sustentação para a nossa prática de meditação. Juntos, os dois operam
em uníssono, culminando com a concentração e o insight. Assim, para capturar na
própria mente a energia do amor bondade a pessoa precisa se aperfeiçoar através
da prática da meditação da atenção plena. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A observação atenta dos
nossos estados mentais pode fazer com que tenhamos consciência do dano,
destruição e dor causados por certos tipos de pensamentos. Enquanto que outros
são<span style="mso-spacerun: yes">  </span>cheios de paz e alegria. Com isso,
a nossa mente passa a rejeitar aquilo que é prejudicial<span
style="mso-spacerun: yes">  </span>e a cultivar o que é pacífico e prazenteiro.
Não aprendemos isso nos<span style="mso-spacerun: yes">  </span>livros, com
mestres, amigos ou inimigos, mas através da nossa própria prática e
experiência. Quando os pensamentos prejudiciais surgem aprendemos a não lhes
dar atenção e quando pensamentos benéficos surgem nós permitimos que eles se
expandam e permaneçam por mais tempo na mente. Assim aprendemos com a nossa
própria experiência como pensar de forma mais saudável. Essa prática cria as
condições na nossa mente para que o amor bondade cresça. Isso significa que
pensamentos benéficos que surgem na nossa mente por si<span
style="mso-spacerun: yes">  </span>mesmos, podem ser gerados de acordo com a
nossa própria vontade, mais tarde. Essa prática nos ajuda a compreender que o
amor bondade pode se desenvolver e crescer no fundo da nossa mente. Fatores
ambientais ou circunstanciais desempenham um papel importante nesse cultivo.
Nenhum ser humano está totalmente desprovido de amor bondade, não importa o
quão cruel ele ou ela aparentem ser. O amor bondade escondido no subconsciente
de cada pessoa deve ser trazido à tona através do desenvolvimento da habilidade
da atenção plena.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Mitra&quot; na
literatura védica e &quot;Mitta&quot; na literatura em Pali significam o sol. A
natureza do sol pode ser chamada de &quot;Maitri&quot; ou &quot;Metta&quot;.
Maitri ou Metta também significam amor bondade ou amizade. Talvez a razão porque
o amor bondade seja assim chamado é porque gera sentimentos calorosos em
relação a todos os seres. Da mesma forma como o calor vem do sol, aquele que
possui amor bondade possui uma coração pleno de calor para com todos. Da mesma
forma como o sol brilha sem discriminação sobre todos os objetos no mundo,
&quot;Metta&quot; ou &quot;Maitri&quot; permeiam todos os seres sem qualquer
discriminação. Da mesma forma como o sol dissipa a escuridão, o amor bondade
destrói a escuridão da raiva. Da mesma forma como alguns objetos absorvem
melhor o sol do que outros, alguns seres vivos absorvem melhor o amor bondade
do que outros. Os seres que absorvem mais amor bondade do que outros são
aqueles que aprendem a relaxar devido ao seu Kamma.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O Buda cultivou <i
style='mso-bidi-font-style:normal'>metta </i>tão potente que o fez amar o seu
pior inimigo, Devadatta, que tentou matá-lo várias vezes. Ele amava o
assaltante e assassino Angulimala, que também tentou matá-lo. Ele amava
Dhanapala, um elefante que tentou matá-lo. Ele amava a todos da mesma forma
como amava o seu filho Rahula. Quando Devadatta morreu, ao ir ter com o Buda,
os monges perguntaram ao Buda qual seria o seu futuro. O Buda disse que no
futuro ele se converteria num Buda silencioso. Assim é o amor bondade, guiado
pela atenção plena, que nos permite viver em paz e harmonia.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O amor bondade ou <i
style='mso-bidi-font-style:normal'>metta </i>não pode ser cultivado pela mera
repetição de palavras de amor bondade. Esse tipo de repetição é muito parecido
com a repetição de uma receita para um paciente num hospital ou de um cardápio
para uma pessoa faminta em um restaurante. Repetir uma lista de coisas nunca
irá produzir o resultado tangível das palavras da lista. O amor bondade é algo
que tem que ser cultivado com intenção por nós mesmos nas nossas mentes.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O amor bondade é
desenvolvido através da meditação. Quando a mente está relaxada, o meditador é
capaz de perdoar e esquecer todas as ofensas que foram cometidas contra ele. A
pessoa pode praticar <i style='mso-bidi-font-style:normal'>metta </i>através da
meditação da tranqüilidade <i style='mso-bidi-font-style:normal'>(samatha)</i>.
Mas isso não dura para sempre porque a tranqüilidade que é alcançada é apenas
temporária. O amor bondade cultivado através de vipassana, por outro lado, é
perpétuo, porque os efeitos da meditação vipassana se enraízam profundamente na
mente da pessoa. A meditação vipassana abranda a mente, e o amor bondade,
cultivado em conjunto com o abrandamento da mente, irá lançar raízes profundas
na mente.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quem
pratica a meditação vipassana vê a impermanência nas formas, sensações,
percepções, formações mentais e consciência. Eles podem comparar as mudanças
nesses agregados com aqueles das outras pessoas. Dessa forma eles não vêm
nenhuma pessoa ou coisa com ódio. Se eles perguntarem a si mesmos quem eles
odeiam, eles não irão encontrar nenhum indivíduo para odiar. Da mesma forma,
eles não encontrarão nenhum indivíduo pelo qual possam cultivar o amor bondade.
Tudo que eles percebem é o fenômeno do fluxo contínuo dos eventos que ocorrem a
cada momento, consigo mesmo e com os outros. Isso os capacita a perdoar e
esquecer as ofensas que outros agregados tenham cometido contra eles, ou seus amigos,
ou parentes. A meditação de <i style='mso-bidi-font-style:normal'>metta </i>é o
processo verdadeiro que genuinamente desenvolve as nossas nobres qualidades,
que podem promover a paz e a felicidade. Não podemos inculcar amor bondade nas
mentes das outras pessoas. Nem as outras pessoas podem fazer o mesmo conosco.
Você não poderá inculcar amor bondade em mim pela força se o meu Kamma impedir
minha mente de aceitá-lo. Cada um de nós precisa preparar o terreno para que o
amor bondade se desenvolva e cresça dentro da nossa mente.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Você também<span
style="mso-spacerun: yes">  </span>deve tê-lo dentro de si antes que possa
ensiná-lo aos outros, da mesma forma como você não pode ensinar algo para
alguém se você não conhecer o assunto primeiro. Suponha que você tente ensinar
algo para outras pessoas sem conhecer o assunto. Você irá passar por tolo.
Quanto mais você conhecer o seu assunto, melhor será para ensiná-lo aos outros.
Da mesma forma, quanto mais treinada for a sua mente na disciplina do amor
bondade,<span style="mso-spacerun: yes">  </span>melhor será para ensiná-la ao
mundo como cultivá-la. É óbvio que você não precisa esperar até que o seu
aprendizado e treinamento estejam completos para iniciar o seu ensino. Enquanto
pratica o amor bondade você estará ganhando experiência prática. Você não
poderá praticar no vazio. Tem que haver outros seres vivos para que você possa praticar
de forma a ganhar experiência. Portanto, ao mesmo tempo que você treina
praticando o amor bondade, estará também treinando os outros nessa prática.
Enquanto ensina você aprende. Enquanto aprende você ensina.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Até mesmo os Bodisatvas,
enquanto se empenham com vigor na própria salvação, ajudam o mundo. A sua
prática os auxilia a alcançar a iluminação primeiro, para assim serem capazes
de ajudar o mundo a alcançar o mesmo objetivo. Se eles ensinarem os outros a
praticar o amor bondade sem que eles mesmos o pratiquem, eles não alcançarão a
iluminação, nem serão capazes de ajudar os outros a praticar o amor bondade.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Cada um de nós precisa
cultivá-lo por si mesmo e para si mesmo. Você não pode cultivá-lo pelos outros.
Nem os outros podem cultivá-lo por você. Se eu prometer salvá-lo praticando o
amor bondade sem que você o pratique, então somente eu livrarei a minha mente
da má vontade, pois não serei capaz de livrar a sua mente desse estado
negativo. Da mesma forma, se você cultivar o amor bondade para mim e eu
cultivá-lo para você, então, ambos estaremos praticando. Eu não deveria esperar
que você pratique o amor bondade por mim. Nem você deveria esperar que eu o
pratique por você. Se você disser, &quot;Não pratique o amor bondade que eu o
farei por você&quot;, isso não funciona. Não diga &quot;Como posso cultivar
amor bondade por tal pessoa que me odeia?&quot; Se você odeia quem lhe odeia,
ambos estarão fazendo o mal. Ao fazer essa pergunta o que você está dizendo, em
outras palavras é, &quot;Como posso ser bom se os outros são maus?&quot; Ou
&quot;Como posso evitar cometer crimes se os outros cometem crimes?&quot; Você
não pratica amor bondade porque os outros o cultivam. Você quer cultivá-lo
justamente porque os outros não o cultivam.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em última análise a prática
do amor bondade depende do desenvolvimento espiritual e do Kamma da pessoa. As
mentes de algumas pessoas possuem um Kamma tão desafortunado que para elas é
quase impossível sonhar quanto à eficácia do amor bondade quanto mais
praticá-lo, pois o seu Kamma impede que elas vejam o benefício do amor bondade.
Se você for ensinar uma classe de alunos irá notar que o desempenho dos alunos
não é igual. Mesmo gêmeos idênticos possuem desempenhos distintos. A
individualidade é a forma de expressar o desenvolvimento emocional,
intelectual, físico e espiritual condicionado pelo Kamma da pessoa. Nem mesmo o
Buda pode interferir no Kamma de uma pessoa. Nós não somos criados iguais,
nascemos diferentes uns dos outros de acordo com o nosso próprio Kamma que
divide os indivíduos com qualidades superiores e inferiores. Se você praticar
um ato com bom Kamma e desfrutar do seu resultado, eu não serei capaz de
roubá-lo ou tomá-lo de você seja pela força ou por meios amigáveis. Se eu
praticar o amor bondade para todos os seres poderei limpar a minha mente da raiva.
Assim a prática do amor bondade se manifesta no meu comportamento. Começar a
prática do amor bondade é começar a prática de bom Kamma, pois nenhum bom Kamma
pode ser praticado sem amor bondade. Ao apresentar os <a
href="../sutta/MN10.php">Quatro Fundamentos da Atenção Plena</a> o Buda disse
aos meditadores que superassem a cobiça e a raiva e o motivo é que durante a
prática da atenção plena o meditador irá encontrar muitos problemas com ambos,
a cobiça e a raiva. Com freqüência as pessoas perguntam &quot;Podemos eliminar
a dor, o sofrimento e a raiva das outras pessoas através do cultivo interior de
amor bondade?&quot; Até mesmo o Buda é incapaz de erradicar a dor e o
sofrimento de outras pessoas desejando-lhes a paz e a felicidade. O Buda disse:
&quot;Você precisa trabalhar a sua própria salvação. Os Budas apenas
ensinam&quot;. Como cada indivíduo possui a sua parcela de Kamma, cada um tem
que se dedicar para obter a sua própria salvação. Se pudéssemos eliminar o
sofrimento dos outros desejando que eles se libertem da dor e do sofrimento,
então fazer com que todo o mundo tivesse paz e felicidade seria muito fácil. Se
isso fosse possível então da mesma forma, também deveria ser possível que uma
pessoa vingativa destruísse todos os seus inimigos desejando-lhes: &quot;Que
eles sejam feios, que eles sintam dores, que eles não sejam prósperos, que eles
não sejam ricos, que eles não sejam famosos, que eles não tenham amigos e que
eles após a morte renasçam em estados miseráveis de existência&quot;. Na
realidade, aqueles que fazem esses tipos de desejos inábeis é que poderão ficar
feios, com dores, sem prosperidade, sem riqueza, sem fama, sem amigos e após a
morte poderão renascer em um estado miserável de existência, porque eles
cometem um Kamma ruim com a mente, por terem um desejo consciente cheio de
raiva. Pensamentos ruins têm o poder de fazer com que os outros se sintam mal e
bons pensamentos têm o poder de fazer com que se sintam bem. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Você pode se perguntar,
&quot;Se no sentido mais fundamental não existem seres, ou em nenhum sentido algum
tipo de eu, ou se existem seres em um sentido condicional e a minha prática de
amor bondade não elimina a sua dor e sofrimento, devido ao seu próprio Kamma,
porque devo cultivar o amor bondade?&quot; Você deve se lembrar que quando a
sua mente está cheia de pensamentos ruins ou pensamentos cheios de raiva por
exemplo, você fala de uma forma rude com a<span style="mso-spacerun: yes"> 
</span>linguagem grosseira, praguejando, difamando e caluniando. Você fala de
forma maldosa. Quando a sua mente está cheia de ódio, tudo que você vê gera
dor; tudo que você ouve gera dor; tudo que você cheira gera dor; tudo que você
come faz com que fique enfermo, tudo que você toca é desagradável para o corpo;
e tudo que você pensa é doloroso. Você se torna vingativo. Você sempre fala mal
dos outros, nunca vê nada de bom nos outros. Você se torna muito crítico. Você
sempre encontra defeitos nos outros. Você nunca aprecia as coisas boas que eles
fazem. Você estará enciumado todo o tempo. Você se torna muito arrogante,
ingrato, malvado, com a mente muito mal intencionada. Você sempre pensa em
causar dano aos outros. Você se delicia em ver os outros sofrerem, com
problemas, com dificuldades. Você fica muito feliz em ver os outros fracassarem
na vida. Assim o seu comportamento ultraja as pessoas. Facilmente você faz com que
as outras pessoas se sintam mal. O seu comportamento será muito desagradável
para as outras pessoas. Todos que estiverem ao seu redor sentirão nojo em ter
que trabalhar com você. Elas ficarão com dor de estômago e dor de cabeça. Elas
ficarão muito nervosas por estarem próximas de você. É assim que os seus
pensamentos inábeis afetam as outras pessoas.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Por outro lado se a sua
mente estiver plena com amor bondade, você irá falar de forma gentil, bondosa e
amigável. Tudo que você ver será motivo para alegria; tudo que você ouvir será
agradável. A sua comida terá um sabor melhor. Tudo que você tocar fará com que
fique satisfeito. Tudo que você cheirar será agradável. Tudo que você pensar
será muito agradável e tranqüilo. Você não medirá esforços para ajudar as
outras pessoas. Você se tornará bastante compreensivo e circunspecto. Você terá
muita paciência. Você se tornará muito amoldável. Você sempre dirá a verdade.
Você sempre irá procurar agradar aos outros. Você estará preparado para
esquecer o mal que lhe fizeram e perdoar as pessoas. Você sempre estará
relaxado. Você não terá um riso falso e desnecessário, mas um sorriso amigável.
Assim as pessoas irão amar trabalhar com você. Elas se sentirão confortáveis em
sua companhia. As suas mentes também se tornarão suaves e gentis em relação a
você. Elas protegerão você. Elas não falarão mal de você pelas suas costas mas,
sim, bem. O seu nível de produtividade aumentará. A sua reputação aumentará.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Além disso, você poderá
perguntar &quot;Qual é o proveito de praticar o amor bondade para com todos os
seres dizendo: ‘Todos os seres vivos que existem, fracos ou fortes, sem
exceção, compridos, grandes, médios, curtos, sutis, grosseiros, visíveis e
invisíveis, próximos e distantes, nascidos e por nascer: que todos os seres
tenham os corações plenos de bem-aventurança!' Porque se deveria desejar, ‘Que
ninguém engane ou despreze outrem, em nenhum lugar, ou devido à raiva ou
inimizade deseje que alguém sofra. Tal qual uma mãe, colocando em risco a
própria vida, ama e protege o seu filho, o seu único filho, da mesma forma,
abraçando todos os seres,<span style="mso-spacerun: yes">  </span>cultive um
coração sem limites.’ Porque alguém deveria ‘Com amor bondade para todo o
universo, cultive um coração sem limites: acima, abaixo e em toda a volta, desobstruído,
livre da raiva e da inimizade. Quer seja parado, andando, sentado, ou deitado,
sempre que estiver desperto, cultive essa atenção plena?'&quot;<span
style="mso-spacerun: yes">  </span><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Algumas vezes, também, as
pessoas se perguntam como podemos desejar para os nossos inimigos &quot;Que os
meus inimigos possam estar bem, felizes e tranquilos. Que nenhum mal lhes
aconteça; que eles não enfrentem nenhuma dificuldade; que eles não enfrentem
nenhum problema; que eles sejam sempre bem sucedidos. Que eles também tenham
paciência, coragem, compreensão e determinação para enfrentar e superar as
inevitáveis dificuldades, problemas e fracassos da vida?&quot;<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Precisamos lembrar que
praticamos amor bondade para a purificação das nossas próprias mentes, da mesma
forma que praticamos a meditação para alcançar a iluminação. Ao desenvolver
amor bondade interiormente poderei me comportar de maneira mais amigável, sem
preferências, preconceitos, discriminação ou ódio. O meu nobre comportamento
permite que eu auxilie os demais seres condicionados, da forma mais prática,
para reduzir a sua dor e o seu sofrimento. São as pessoas compassivas que se
comportam de forma suave e gentil para fazer com que as pessoas ao seu redor se
sintam confortáveis. Compaixão é a manifestação do amor bondade em ação, pois
quem não possui amor bondade é incapaz de ajudar os outros. Comportamento nobre
significa portar-se da forma mais cordial e amigável possível. O comportamento
inclui os pensamentos, linguagem e ações corporais. Se houver contradições
nessa forma tríplice de expressar o nosso comportamento, então existe algo de
errado nele. O comportamento contraditório não pode ser um comportamento nobre.
Se alguém fala de amor bondade mas se comporta de uma forma grosseira, ele/ela
é hipócrita<span style="mso-spacerun: yes">  </span>não é honesto/a.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Por outro lado, falando em
termos pragmáticos, é muito melhor cultivar o nobre pensamento, &quot;Que todos
os seres sejam felizes&quot; do que o pensamento &quot;Eu o odeio&quot;. O
pensamento nobre irá definitivamente encontrar a sua expressão no nosso nobre
comportamento e o nosso pensamento maldoso irá encontrar a sua expressão no mau
comportamento.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Precisamos lembrar que os
nossos pensamentos são transformados em linguagem e ação de modo a alcançar o
resultado esperado. A intenção ou pensamento traduzido em ação é capaz de
produzir um hormônio positivo no nosso cérebro. Esse hormônio positivo age como
um nutriente que alimenta e fortifica os nossos nervos. Quando os nossos nervos
são estimulados com esse hormônio positivo eles se fortalecem. Esse hormônio
positivo será também transportado através do nosso corpo pela circulação
sanguínea, fazendo com que as nossas células sejam saudáveis. Os nervos e
células saudáveis fazem com que o nosso corpo e mente sejam fortes e saudáveis.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Devemos sempre falar e
fazer as coisas com atenção plena no amor bondade. Se, enquanto fala de amor
bondade, você age ou fala de uma forma diametralmente oposta, você será
repreendido pelos sábios. Na medida em que a atenção plena no amor bondade se
desenvolve, os nossos pensamentos, palavras e ações se tornam gentis,
agradáveis, relevantes, verdadeiros e benéficos para nós, bem como para os
demais. Se os nossos pensamentos, palavras ou ações causam dano para nós, para
os outros, ou ambos, então precisamos nos questionar se realmente estamos
plenamente atentos ao amor bondade.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em termos práticos, se
todos os seus inimigos estivessem bem, felizes e tranqüilos, eles não seriam
seus inimigos. Se eles estiverem livres de problemas, dores, sofrimento,
aflições, neuroses, psicoses, paranóias, temores, tensões, ansiedades, etc.,
eles não serão mais os seus inimigos. A sua solução prática para os seus
inimigos é auxiliá-los a superar os problemas deles, de forma que você possa
viver em paz e feliz. Na verdade, se você pudesse, você deveria preencher as
mentes de todos os seus inimigos com amor bondade e fazer com que todos eles
realizem Nibbana, assim você poderia viver em paz e feliz. Quanto mais
neuroses, psicoses, temores, tensões, ansiedades, etc eles tiverem, tanto mais
problemas, dores e sofrimento eles poderão causar ao mundo. Se você conseguir
converter uma pessoa má e cruel em uma pessoa pura e santa então você terá
realizado um milagre que o Buda nos permitiu realizar. Cultivemos a sabedoria e
o amor bondade nas nossas mentes para converter as mentes más em mentes santas.</span><span
lang=PT-BR style='font-size:11.0pt;mso-bidi-font-size:12.0pt;mso-ansi-language:
PT-BR'><o:p></o:p></span></p>


