<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Em Savatthi. "Bhikkhus, da ignorância como condição, as formações volitivas [surgem]. Das formações volitivas como condição, a consciência. Da consciência como condição, a mentalidade-materialidade (nome e forma) ... Essa é a origem de toda essa massa de sofrimento." </p>

<p class=Normal style='margin-left:.75in'>Quando isso foi dito, um certo bhikkhu perguntou ao Abençoado: </p>

<p class=Normal style='margin-left:.75in'>"Venerável senhor, o que é o envelhecimento e morte? A quem eles pertencem?" </p>

<p class=Normal style='margin-left:.75in'>"Não é uma pergunta válida," o Abençoado respondeu. Dizer um dos dois, ‘O que é o envelhecimento e morte, a quem eles pertencem,’ ou ‘envelhecimento e morte são uma coisa, quem experimenta isso é outra,’ ambas afirmações são idênticas no significado; elas diferem apenas no fraseado. Quando existe o entendimento, ‘a alma e o corpo são a mesma coisa,’ não pode haver a vida santa; e quando existe o entendimento, ‘a alma e o corpo são duas coisas distintas,’ não pode haver a vida santa. Evitando esses dois extremos o Tathagata ensina o Dhamma pelo meio: ‘Do nascimento como condição, o envelhecimento e a morte.’"</p>

<p class=Normal style='margin-left:.75in'>"Venerável senhor, nascimento … ser/existir … apego … desejo … sensação … contato … as bases dos sentidos … mentalidade-materialidade (nome e forma) … consciência… formações volitivas … O que é tudo isso? A quem tudo isso pertence?" </p>

<p class=Normal style='margin-left:.75in'>"Não é uma pergunta válida," o Abençoado respondeu. Dizer um dos dois, ‘O que são as formações volitivas, a quem elas pertencem,’ ou ‘as formações volitivas são uma coisa, quem experimenta isso é outra,’ ambas afirmações são idênticas no significado; elas diferem apenas no fraseado. Quando existe o entendimento, ‘a alma e o corpo são a mesma coisa,’ não pode haver a vida santa; e quando existe o entendimento, ‘a alma e o corpo são duas coisas distintas,’ não pode haver a vida santa. Evitando esses dois extremos o Tathagata ensina o Dhamma pelo meio: ‘Da ignorância como condição, as formações volitivas.’</p>

<p class=Normal style='margin-left:.75in'>"Mas com o desaparecimento e cessação sem deixar vestígios dessa mesma ignorância, qualquer tipo de contorção, manobra, vacilação que possa haver - ‘O que é o envelhecimento e morte, a quem eles pertencem,’ ou ‘envelhecimento e morte são uma coisa, quem experimenta isso é outra,’ ou ‘a alma e o corpo são a mesma coisa,’ ou ‘a alma e o corpo são duas coisas distintas,’ - tudo isso é abandonado, cortado pela raiz, feito como com um tronco de palmeira, eliminado de modo que não estará sujeito a um futuro surgimento. </p>

<p class=Normal style='margin-left:.75in'>"Mas com o desaparecimento e cessação sem deixar vestígios dessa mesma ignorância, qualquer tipo de contorção manobra, vacilação que possa haver - ‘O que é o nascimento, a quem ele pertence,’ ou ‘nascimento é uma coisa, quem experimenta isso é outra,’ ... ‘O que são as formações volitivas, a quem elas pertencem,’ ou ‘as formações volitivas são uma coisa, quem experimenta isso é outra,’ou ‘a alma e o corpo são a mesma coisa,’ ou ‘a alma e o corpo são duas coisas distintas,’ - tudo isso é abandonado, cortado pela raiz, feito como com um tronco de palmeira, eliminado de modo que não estará sujeito a um futuro surgimento." [SN.II.61] (<a href="../sutta/SNXII.35.php">SN XII.35</a>)<o:p></o:p></span></p>


<p class=Normal align=center style='text-align:center;margin-left:.75in'><span
lang=PT-BR style='mso-ansi-language:PT-BR'>* * *<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>&quot;Pergunta: Quem realiza o contato?<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Resposta: Essa não é uma pergunta válida. Eu não digo ‘alguém realiza o contato.’ 
Se eu dissesse ‘alguém realiza o contato,’ então nesse caso esta seria uma pergunta válida: 
‘Venerável senhor, quem realiza o contato?’ Mas eu não falo dessa forma. Visto que não 
falo dessa forma, se alguém me perguntar, ‘Venerável senhor, com o que como condição o 
contato [surge]?’ essa seria uma pergunta válida. A resposta válida a essa pergunta é: 
‘Com as seis bases como condição, o contato [surge]; com o contato como condição, a sensação<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>P: Quem sente?<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>R: Essa não é uma pergunta válida. Eu não digo ‘alguém sente.’ 
Se eu dissesse ‘alguém sente,’ então nesse caso esta seria uma pergunta válida: 
‘Venerável senhor, quem sente?’ Mas eu não falo dessa forma. Visto que não falo 
dessa forma, se alguém me perguntar, ‘Venerável senhor, com o que como condição a 
sensação [surge]?’ essa seria uma pergunta válida. A resposta válida a essa pergunta é: 
‘Com o contato como condição, a sensação [surge]; com a sensação como condição, o desejo. [SN.II.I3] (<a href="../sutta/SNXII.12.php">SN XII.12</a>)<o:p></o:p></span></p>

<p class=Normal align=center style='margin-left:.75in;text-align:center'><span
lang=PT-BR style='mso-ansi-language:PT-BR'>* * *<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Bhikkhus, este corpo não lhes pertence, nem pertence a um outro. Vocês
devem vê-lo como kamma passado, formado por condições, nascido das volições, a
base para as sensações.”.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Com relação a isso, bhikkhus, o nobre discípulo bem instruído,
considera de modo sábio a origem dependente de todas as coisas, assim, ‘Quando
existe isso, aquilo existe; Com o surgimento disso, aquilo surge. Quando não
existe isso, aquilo também não existe; Com a cessação disto, aquilo cessa.’
Isto é, ‘Da ignorância como condição, as formações volitivas [surgem]. Das formações volitivas como
condição, a consciência...do desaparecimento e cessação sem deixar vestígios
dessa mesma ignorância, cessam as formações volitivas. Da cessação das formações volitivas,<span
style='mso-spacerun:yes'>  </span>cessa a consciência…'&quot; [SN.II.64] (<a
href="../sutta/SNXII.37.php">SN XII.37</a>)<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O
ensinamento da Origem Dependente mostra a verdade de todas as coisas na natureza
como tendo as características de impermantes, insatisfatórias e não-eu, </span><a
href="#Aster"><span lang=PT-BR style='mso-ansi-language:PT-BR'>[*</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>] <a name=RAster></a>e prosseguindo
de acordo com causa e efeito. Não existe necessidade de questionar a existência
ou não existência das coisas, se elas são eternas ou se são aniquiladas e assim
por diante, pois esse tipo de questões não fazem parte daquilo que é
verdadeiramente útil. No entanto, sem o claro entendimento da Origem
Dependente, as Três Características, em especial o não-eu, também será mal
interpretado. Com freqüência o ensinamento do não-eu é interpretado como
significando o nada, que está de acordo com a visão niilista (<i>natthika</i>),
uma forma particularmente perniciosa de entendimento incorreto.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Além de
ajudar a evitar esse tipo de idéia, a compreensão clara do princípio da Origem
Dependente irá evitar o surgimento de idéias acerca de uma Gênese ou Primeira
Causa,<span style='mso-spacerun:yes'>  </span>tal como mencionado no início
deste livro. Algumas palavras do Buda a esse respeito:<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Quando um nobre discípulo viu bem, com correta sabedoria, essa 
origem dependente e esses fenômenos com origem dependente, da forma como 
eles na verdade são, não é possível que ele regresse ao passado, pensando, 
‘Eu existi no passado? Não existi no passado? O que fui no passado? Como eu 
era no passado? Tendo sido que, no que me tornei no passado? Existirei no futuro? 
Não existirei no futuro? O que serei no futuro? Como serei no futuro? Tendo sido que, 
no que me tornarei no futuro?’ ou que ele esteja no seu íntimo perplexo acerca do presente: 
‘Eu sou? Eu não sou? O que sou? Como sou? De onde veio este ser? Para onde irá?’ Isso não é possível.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Porque isso? Porque um nobre discípulo viu bem, com correta sabedoria, 
essa origem dependente e esses fenômenos com origem dependente, da forma como eles na verdade são.”[SN.II.26] (<a
href="../sutta/SNXII.20.php">SN XII.20</a>)<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Nesse
contexto, alguém que vê o princípio da Origem Dependente não estará mais
inclinado a especular acerca de questões metafísicas. É por isso que o Buda
permanecia em silêncio em relação a esses temas. Ele chamava esse tipo de
questões <i>abyakatapañha</i> – questões que é melhor deixar sem resposta. Ao
ver o princípio da Origem Dependente, e compreender como todas as coisas fluem
ao longo do contínuo de causa e efeito, essas questões perdem todo o sentido.
Aqui vamos considerar algumas das razões porque o Buda não respondia essas
questões:<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Então o errante Vacchagotta foi até o Abençoado e ambos se cumprimentaram. Quando a conversa cortês e amigável havia terminado, ele sentou a um lado e disse:<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> "Como é, Mestre Gotama, o mundo é eterno’?” </p>

<p class=Normal style='margin-left:.75in'> “Vaccha, eu não declarei isso: o mundo é eterno.” </p>

<p class=Normal style='margin-left:.75in'> “Nesse caso, o mundo não é eterno?” </p>

<p class=Normal style='margin-left:.75in'> “Vaccha, eu não declarei isso: o mundo não é eterno." </p>

<p class=Normal style='margin-left:.75in'> “Nesse caso, o mundo é finito?” </p>

<p class=Normal style='margin-left:.75in'> “Vaccha, eu não declarei isso: o mundo é finito.” </p>

<p class=Normal style='margin-left:.75in'> “Nesse caso, o mundo é infinito?” </p>

<p class=Normal style='margin-left:.75in'> “Vaccha, eu não declarei isso: o mundo é infinito.” </p>

<p class=Normal style='margin-left:.75in'> “Nesse caso, a alma e o corpo são a mesma coisa?” </p>

<p class=Normal style='margin-left:.75in'> “Vaccha, eu não declarei isso: a alma e o corpo são a mesma coisa.” </p>

<p class=Normal style='margin-left:.75in'> “Nesse caso, a alma é uma coisa e o corpo outra?” </p>

<p class=Normal style='margin-left:.75in'> “Vaccha, eu não declarei isso: a alma é uma coisa e o corpo outra.” </p>

<p class=Normal style='margin-left:.75in'> “Nesse caso, após a morte um Tathagata existe?” </p>

<p class=Normal style='margin-left:.75in'> “Vaccha, eu não declarei isso: após a morte um Tathagata existe.” </p>

<p class=Normal style='margin-left:.75in'> “Nesse caso, após a morte um Tathagata não existe’?” </p>

<p class=Normal style='margin-left:.75in'> “Vaccha, eu não declarei isso: após a morte um Tathagata não existe.” </p>

<p class=Normal style='margin-left:.75in'> “Nesse caso, após a morte um Tathagata tanto existe como não existe?” </p>

<p class=Normal style='margin-left:.75in'> “Vaccha, eu não declarei isso: após a morte um Tathagata tanto existe como não existe.” </p>

<p class=Normal style='margin-left:.75in'> “Nesse caso, após a morte um Tathagata nem existe nem não existe?” </p>

<p class=Normal style='margin-left:.75in'> “Vaccha, eu não declarei isso: após a morte um Tathagata nem existe nem não existe.” </p>

<p class=Normal style='margin-left:.75in'> “Mestre Gotama, qual é a causa e razão pela qual, quando os contemplativos de outras seitas são assim questionados, eles dão respostas como: 'o mundo é eterno' ...  'após a morte um Tathagata nem existe nem não existe'. Qual é a causa e razão pela qual, quando o Mestre Gotama é assim questionado ele não dá esse tipo de resposta? </p>

<p class=Normal style='margin-left:.75in'>Vaccha, os errantes de outras seitas consideraram que a forma é o eu, ou o eu possuído de forma, ... ou a forma como estando no eu, ... ou o eu como estando na forma. Eles consideram que a sensação é o eu, ou o eu possuído de sensação, ... ou a sensação como estando no eu, ... ou o eu como estando na sensação. Eles consideram que a percepção seja o eu, ou o eu possuído de percepção, ... ou a percepção como estando no eu, ... ou o eu como estando na percepção. Eles consideram que as formações sejam o eu, ou o eu possuído de formações, ... ou as formações como estando no eu, ... ou o eu como estando nas formações. Eles consideram que a consciência seja o eu, ou o eu possuído de consciência, ... ou a consciência como estando no eu, ... ou o eu como estando na consciência. Portanto, quando são assim questionados, eles dão respostas como: 'o mundo é eterno' ...  'após a morte um Tathagata nem existe nem não existe'. Mas Vaccha, o Tathagata, um arahant perfeitamente iluminado não considera que a forma é o eu ... ou o eu como estando na consciência. Por conseguinte, quando o Tathagata é assim questionado ele não dá esse tipo de resposta." [</span><a
href="#N22"><span lang=PT-BR style='mso-ansi-language:PT-BR'>22</span></a><span
lang=PT-BR style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>

<p class=Normal><a name=R22></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Existe uma grande quantidade de outras teorias ou escolas que têm uma
relação especial com o conceito de kamma e que também conflitam com o princípio
da Origem Dependente. Esses casos estão cobertos no livro </span><a
href="kamma_1.php"><span lang=PT-BR style='mso-ansi-language:PT-BR'>Kamma nos
Ensinamentos do Buda</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>.<o:p></o:p></span></p>

