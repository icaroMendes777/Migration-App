<p class=Tit1 align=center style='text-align:center'>8. O Ensinamento do Meio </Tit1>

<p class=Normal>&nbsp;</p>

<hr size=2 width="100%" align=center>

<p class=Normal>&nbsp;</p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Compreender
o princípio da Origem Dependente, diz-se, é ter o Entendimento Correto (<i
style='mso-bidi-font-style:normal'>sammaditthi</i>). Esse Entendimento Correto
é um tipo de entendimento equilibrado que não tende para os extremos. Portanto,
o princípio da Origem Dependente é uma lei que ensina a verdade de modo medial
e imparcial, conhecido como o Ensinamento do Meio. A mediania dessa verdade
pode ser entendida com mais clareza quando comparada com outros ensinamentos.
Para demonstrar como o princípio da Origem Dependente difere desses
entendimentos extremados, iremos agora apresentar algumas dessas idéias,
organizadas em pares, empregando as palavras do Buda como explicação e mantendo
os comentários adicionais num nível mínimo.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Primeiro
Par:<o:p></o:p></span></b></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. <i>Atthikavada</i>:</span></b><span
lang=PT-BR style='mso-ansi-language:PT-BR'> A escola que sustenta que todas as
coisas realmente existem (realismo transcendental).<sup><o:p></o:p></sup></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. <i>Natthikavada</i>:</span></b><span
lang=PT-BR style='mso-ansi-language:PT-BR'> A escola que sustenta que todas as
coisas não existem (niilismo).<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR>“Venerável senhor, é dito
‘Entendimento correto, entendimento correto.’ De que forma existe entendimento
correto?”</span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Kaccayana, em geral este mundo depende de uma dualidade, a noção da
existência e a noção da não existência. Mas para aquele que vê com correta
sabedoria a origem do mundo, tal como na verdade ela ocorre, a noção da ‘não
existência’ com relação ao mundo não lhe ocorrerá. Aquele que vê com correta
sabedoria a cessação do mundo, tal como<span style='mso-spacerun:yes'> 
</span>na verdade ela ocorre, a noção da ‘existência’ com relação ao mundo não
lhe ocorrerá.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Kaccayana, em geral este mundo é aprisionado por adesões, apegos e
preconceitos. Mas uma pessoa como essa [com entendimento correto] não se
envolve ou se apega através dessas adesões, apegos, fixações mentais,
inclinações ou obsessões; e ela não toma uma determinação com relação ao ‘meu
eu’; ela não tem dúvida ou perplexidade que aquilo que surge é apenas o
sofrimento surgindo, aquilo que cessa é apenas o sofrimento cessando. O
conhecimento dela com relação a isso não depende dos outros. É com referência a
isso, Kaccayana, que existe o entendimento correto.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“’Tudo existe’: Kaccayana, esse é um extremo. ‘Tudo não existe’: esse é
o outro extremo. Evitando esses dois extremos, o Tathagata ensina o Dhamma pelo
meio: Da ignorância como condição, as formações volitivas [surgem] ...”<span
style='mso-spacerun:yes'>  </span>(<a href="../sutta/SNXII.15.php">SN.XII.15</a>)<o:p></o:p></span></p>


<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-language:PT-BR'>* * *<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Em Savatthi. Então um brâmane foi até o Abençoado e perguntou:</p>

<p class=Normal style='margin-left:.75in'> "Mestre Gotama, 'todas as coisas existem'?" </p>

 <p class=Normal style='margin-left:.75in'> "A idéia de que 'todas as coisas existem', brâmane,  é a cosmologia mais antiga." </p>

<p class=Normal style='margin-left:.75in'>"Então 'todas as coisas não existem'?" </p>

<p class=Normal style='margin-left:.75in'> "A idéia de que 'todas as coisas não existem' é a segunda cosmologia." </p>

<p class=Normal style='margin-left:.75in'>"Então 'tudo é unidade'?" </p>

<p class=Normal style='margin-left:.75in'> "A idéia de que 'tudo é unidade' é a terceira cosmologia." </p>

<p class=Normal style='margin-left:.75in'>"Então 'tudo é pluralidade'?" </p>

<p class=Normal style='margin-left:.75in'>"A idéia de que 'tudo é pluralidade' é a quarta cosmologia. Evitando todos esses extremos, o Tathagata ensina o Dhamma pelo meio, da ignorância como condição, as formações volitivas [surgem] ..."[SN.II.77] (<a href="../sutta/SNXII.48.php">SN XII.48</a>)<o:p></o:p></span></p>

