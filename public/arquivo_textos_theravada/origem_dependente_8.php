<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Em Savatthi. Então o errante Kassapa se aproximou do Abençoado e ambos se cumprimentaram. Depois que a conversa amigável e cortês havia terminado ele sentou a um lado e disse: </p>

<p class=Normal style='margin-left:.75in'> “Como é, Mestre Gotama: o sofrimento é criado pela própria pessoa?” </p>

<p class=Normal style='margin-left:.75in'> “Não é assim, Kassapa,” o Abençoado respondeu. </p>

<p class=Normal style='margin-left:.75in'> “Então, Mestre Gotama, o sofrimento é criado pelos outros?” </p>

<p class=Normal style='margin-left:.75in'> “Não é assim, Kassapa,” o Abençoado respondeu. </p>

<p class=Normal style='margin-left:.75in'> “Como é então, Mestre Gotama: o sofrimento é criado ambos pela própria pessoa e pelos outros?” </p>

<p class=Normal style='margin-left:.75in'> “Não é assim, Kassapa,” o Abençoado respondeu. </p>

<p class=Normal style='margin-left:.75in'> “Então, Mestre Gotama, o sofrimento surge ao acaso, sem ser criado pela própria pessoa e tampouco pelos outros?” </p>

<p class=Normal style='margin-left:.75in'> “Não é assim, Kassapa,” o Abençoado respondeu. </p>

<p class=Normal style='margin-left:.75in'> “Como é então, Mestre Gotama: não existe o sofrimento?” </p>

<p class=Normal style='margin-left:.75in'> “Não é que não existe o sofrimento, Kassapa; o sofrimento existe.” </p>

<p class=Normal style='margin-left:.75in'> “Então é que o Mestre Gotama não conhece e não vê o sofrimento?” </p>

<p class=Normal style='margin-left:.75in'> “Não é que eu não conheço e não vejo o sofrimento, Kassapa. Eu conheço o sofrimento, eu vejo o sofrimento.” </p>

<p class=Normal style='margin-left:.75in'> “Ao ser perguntado: ‘Como é, Mestre Gotama: o sofrimento é criado pela própria pessoa?’ ou ‘Ele é criado pelos outros?’ ou ‘Ele é criado por ambos?’ ou ‘Ele não é criado por nenhum desses’ em cada caso você diz: ‘Não é assim, Kassapa.’ </p>

<p class=Normal style='margin-left:.75in'> "Ao ser perguntado: ‘Como é então, Mestre Gotama: não existe o sofrimento?’ você diz: ‘Não é que não existe o sofrimento, Kassapa; o sofrimento existe.’ Ao ser perguntado: ‘Então é que o Mestre Gotama não conhece e não vê o sofrimento?’ você diz: ‘Não é que eu não conheço e não vejo o sofrimento, Kassapa. Eu conheço o sofrimento, eu vejo o sofrimento.’ Venerável senhor, que o Abençoado me explique o sofrimento. Que o Abençoado me ensine acerca do sofrimento.” </p>

<p class=Normal style='margin-left:.75in'> “Kassapa, [se alguém pensa,] ‘Aquele que age é o mesmo que experimenta o resultado,’ [então ele afirma] a respeito de alguém que existe desde o princípio: ‘O sofrimento é criado pela própria pessoa.’ Quando alguém diz isso, isso é eternalismo. Mas Kassapa, [se alguém pensa,] ‘Aquele que age é um e aquele que experimenta o resultado é outro,’ [então ele afirma] com relação àquele afligido pela sensação: ‘O sofrimento é criado pelos outros.’ Quando alguém diz isso, isso é aniquilação. Evitando esses dois extremos o Tathagata ensina o Dhamma pelo meio: Da ignorância como condição, as formações volitivas [surgem]. Das formações volitivas como condição, a consciência. Da consciência como condição, a mentalidade-materialidade (nome e forma). Da mentalidade-materialidade (nome e forma) como condição, as seis bases dos sentidos. Das seis bases dos sentidos como condição, o contato. Do contato como condição, a sensação. Da sensação como condição, o desejo. Do desejo como condição, o apego. Do apego como condição, o ser/existir. Do ser/existir como condição, o nascimento. Do nascimento como condição, então o envelhecimento e morte, tristeza, lamentação, dor, angústia e desespero surgem. Essa é a origem de toda essa massa de sofrimento. Mas com o desaparecimento e cessação sem deixar vestígios dessa mesma ignorância cessam as formações volitivas. Da cessação das formações volitivas cessa a consciência. Da cessação da consciência cessa a mentalidade-materialidade (nome e forma). Da cessação da mentalidade-materialidade (nome e forma) cessam as seis bases dos sentidos. Da cessação das seis bases dos sentidos cessa o contato. Da cessação do contato cessa a sensação. Da cessação da sensação cessa o desejo. Da cessação do desejo cessa o apego. Da cessação do apego cessa o ser/existir. Da cessação do ser/existir cessa o nascimento. Da cessação do nascimento, então o envelhecimento e morte, tristeza, lamentação, dor, angústia e desespero, tudo cessa. Essa é a cessação de toda essa massa de sofrimento.’” [SN.II.19] (<a href="../sutta/SNXII.17.php">SN XII.17</a>)<o:p></o:p></span></p>


