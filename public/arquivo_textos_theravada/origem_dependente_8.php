<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. <i>Attakaravada</i></span></b><span
lang=PT-BR style='mso-ansi-language:PT-BR'> ou <b><i>Sayankaravada</i></b>: A
escola que sustenta a idéia de que a felicidade e o sofrimento são totalmente
