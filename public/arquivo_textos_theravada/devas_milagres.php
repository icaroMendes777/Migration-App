<p class=Normal>Conheci um americano que queria se tornar um monge budista. Ele ouviu que na Tailândia poderia ser ordenado. </p>

<p class=Normal>Então ele viajou para a Tailândia e se hospedou num pequeno hotel em Bangkok. Mas ele não sabia onde deveria ir para ser ordenado. Então, ele pediu para uma pessoa (um ajudante) que trabalhava no hotel onde poderia ir para ser ordenado como um monge Budista. </p>

<p class=Normal>O ajudante do hotel lhe disse para ir ao templo Wat Pho em Bangkok (onde os homens em Bangkok vão para serem ordenados temporariamente). Ele disse ao americano "vá de manhã cedo e ofereça algumas frutas para um monge que virá esmolar comida, e peça para ser ordenado. Então, ele irá ordená-lo".</p>

<p class=Normal>Assim, o americano saiu cedo na manhã seguinte para ir ao templo com algumas frutas para oferecer ao primeiro monge que viesse. Na pressa, ele tinha ido muito cedo, por volta das 04h30. </p>

<p class=Normal>As portas do templo estavam fechadas. Então ele começou a andar para cá e para lá, perto da entrada até que os monges saíssem de onde estavam meditando na floresta próxima, para esmolar comida pela manhã. </p>

<p class=Normal>Enquanto caminhava para lá e para cá, um homem Tailandês vestido em trajes orientais se aproximou dele e perguntou-lhe em Inglês perfeito o que ele estava esperando. </p>

<p class=Normal>Quando ele explicou o que estava buscando, o homem Tailandês lhe disse: "Eu vou levá-lo para dentro e mostrar-lhe o interior do templo". Ele então pegou da sua cintura um molho com chaves grandes e abriu o portão de ferro. Em seguida, pedindo para  que o americano o seguisse, ele foi até a porta principal do templo e abriu-a com uma chave do molho que tinha. </p>

<p class=Normal>Ao entrar, ele acendeu as luzes, e o americano viu o mais belo santuário com pinturas muito brilhantes decorando as paredes. Ele levou o americano a cada uma das pinturas e explicou o significado de todas. </p>

<p class=Normal>Depois de conduzi-lo em volta do santuário dessa maneira, ele então desligou as luzes e trancou a porta. Depois de sair, ele disse para o americano: "Agora já é quase a hora em que os monges saem para esmolar comida, ofereça as suas frutas e peça para ser ordenado". Em seguida, ele saiu do templo e desapareceu. </p>

<p class=Normal>Depois de um tempo, o americano viu um monge aparecer com uma tigela de esmolar. Ele foi até o monge, ofereceu as frutas e pediu para ser ordenado. O monge aceitou as frutas e o americano foi ordenado. Infelizmente para o americano (que agora era um monge) os monges Tailandeses no templo onde ele estava falavam muito pouco ou nada de Inglês. </p>

<p class=Normal>Depois de ficar no templo por um tempo, certo dia, ele pediu licença ao seu professor para ir conversar com outro monge que soubesse Inglês melhor para receber instruções. Em seguida, o professor disse que não havia nenhum outro monge que falasse Inglês melhor. Ele mesmo falava o melhor Inglês no local. </p>

<p class=Normal>Então, o monge americano se lembrou do homem Tailandês que ele havia conhecido antes de pedir para ser ordenado. Ele disse para o professor "Havia um homem que conheci aqui no dia em que pedi para ser ordenado que falava um Inglês perfeito, eu não poderia pedir os seus serviços como tradutor?" O professor ficou um tanto surpreso e perguntou ao seu pupilo detalhes sobre esse homem. </p>

<p class=Normal>Depois de ouvir a história, ele disse: "Isso é impossível! Apenas o Sangha-Raja da Tailândia tem as chaves desse santuário. Esse é o lugar onde os reis vêm para a ordenação temporária." Então o monge americano foi levado até o Sangha-Raja da Tailândia e lhe pediram para que narrasse a história mais uma vez. Quando ele descreveu as pinturas e as histórias que ouviu relativas às imagens no interior do santuário, o Sangha-Raja não pode duvidar da sua história. Ele foi levado para dentro do santuário para narrar a história e foi questionado sobre como era a aparência do homem Tailandês.

<p class=Normal>Ao olhar mais uma vez as pinturas na parede, o monge americano, para sua surpresa, viu a imagem do homem que ele conheceu adornando a parede. Ele apontou para a pintura e disse "esse é o homem".</p>

<p class=Normal>Aquela era a imagem do rei Rama I, que havia falecido há muitos anos. Supostamente ficou estabelecido que o monge americano tinha conhecido um deva que antigamente havia sido o rei Rama I da Tailândia que também tinha construído o templo. </p>

<p class=Normal>Eu conheci esse monge americano, e eu sei que esta história é verdadeira. Portanto, é verdade que há devas, é verdade que eles existem, mesmo hoje. Não só na Tailândia, mas no Sri Lanka também, bem como, até mesmo na Austrália, onde vivo atualmente. </p>

<p class=Normal><b>Uma Experiência Pessoal</b>

<p class=Normal>Posso lhes contar uma outra história, uma experiência pessoal minha, recente. Certo dia, a Buddhist Society of Western Australia estava se preparando para as celebrações de Vesak. </p>

<p class=Normal>Os preparativos estavam em andamento para realizar as celebrações ao ar livre, na noite no dia de Vesak, em um local central em Perth. Faltando apenas alguns dias, ouvimos que a previsão do tempo não era boa para o dia e a noite do Vesak. A previsão era que haveriam tempestades severas. </p>

<p class=Normal>Com o dia se aproximando, as pessoas responsáveis pela organização do evento estavam com muitas dúvidas de seguir em frente. Como presidente da BSWA recebi muitos pedidos para cancelar o evento. </p>

<p class=Normal><b>Fé Continuada</b></p>

<p class=Normal>Nós tínhamos convidado o primeiro-ministro para vir como um convidado especial, entre muitos outros convidados especiais. Foram muitos telefonemas perguntando-me se o evento seria cancelado. Mas eu continuei a ter fé. </p>

<p class=Normal>Embora as pessoas tivessem se surpreendido com a minha resolução, eu decidi ir em frente com a celebração, apesar dos pedidos de muitos quadrantes para cancelá-lo. </p>

<p class=Normal>O dia amanheceu com nuvens carregadas. Na medida em que o dia avançava as condições não melhoraram. Começou a chover forte e continuou ao anoitecer. Ouvimos no noticiário que as condições climáticas nas áreas vizinhas também eram muito ruins. </p>

<p class=Normal>Estava pior do que as previsões tinham anunciado. No entanto, na medida em que a noite avançava, e foi se aproximando o momento em que nós queríamos começar as comemorações, a tempestade cessou e o céu ficou limpo, como que por mágica. A lua cheia surgiu em todo o seu esplendor e iluminou a área das celebrações. </p>

<p class=Normal><b>Celebração do Vesak</b></p>

<p class=Normal>Todos aqueles que ali se reuniram naquele dia experimentaram uma celebração de Vesak calma e serena. O clima perfeito prevaleceu até que as comemorações terminaram e todos foram embora para as suas casas. Posteriormente, a tempestade recomeçou. E nós descobrimos que, mesmo durante o tempo da celebração, a tempestade continuou enfurecida nas áreas circundantes. </p>

<p class=Normal>Eu sei que este milagre teve o auxílio dos Devas, que nos permitiu ter a celebração de Vesak sem qualquer impedimento. </p>

<p class=Normal>Portanto, não é uma mentira que os Devas existem, e eles ajudam os seguidores puros do Dhamma. </p>

