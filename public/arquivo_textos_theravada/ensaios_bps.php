     <a
     href="ensaio27.php">O Dhamma e a Não-dualidade ( 1994, 1995)</a></span>.</b>
      Ao contrário
     do objetivo manifesto de algumas outras religiões e práticas orientais
     populares na atualidade, o Dhamma <i style='mso-bidi-font-style:normal'>não
     </i>se preocupa com o atingimento de um estado de
     &quot;não-dualismo&quot;, uma condição na qual as barreiras entre o
     &quot;eu&quot; e o &quot;outro&quot;, ou <i style='mso-bidi-font-style:
     normal'>samsara</i> e <i style='mso-bidi-font-style:normal'>nibbana,</i>
     finalmente se dissolvem. Tampouco os ensinamentos sustentam o &quot;dualismo&quot;
     - ou, na verdade, nenhum tipo de <i style='mso-bidi-font-style:normal'>ismo.</i>
     Os ensinamentos se preocupam apenas com o sofrimento, a sua causa, a sua
     cessação e o caminho que conduz à sua cessação.<o:p></o:p></span></li>
</ul>


