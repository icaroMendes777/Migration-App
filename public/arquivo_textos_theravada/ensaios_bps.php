<p class=Tit1 align=center style='text-align:center'>Bhikkhu Bodhi</Tit1>

<p class=Normal>A maioria
dos textos aqui apresentados são ensaios curtos ( 1 – 3 pag.) escritos pelo
Bhikkhu Bodhi para a BPS Newsletter que oferecem reflexões aguçadas e
esclarecedoras acerca de um amplo espectro de tópicos do Dhamma com relevância
imediata para praticantes Budistas. Tomando por base a erudição e as
escrituras, cada ensaio aborda um tema relativo aos princípios e conceitos
básicos do Budismo Theravada. Muitos dos ensaios servem para esclarecer e
desfazer confusões acerca de alguns dos pontos cruciais do Dhamma que são
freqüentemente mal entendidos no mundo do Budismo popular. <o:p></o:p></span></p>

<p class=Normal>Bhikkhu
Bodhi é um monge Budista Americano nascido em Nova Iorque em 1944. Formado em
Filosofia pelo Brooklyn College em 1966 e com PhD em Filosofia pelo Claremont
Graduate School em 1972. Ele foi ordenado no Sri Lanka como samanera no final
de 1972 e bhikkhu em 1973 pelo falecido Venerável Balangoda Ananda Maitreya
Mahanayaka Thera. Em 1977 ele retornou aos Estados Unidos onde passou dois anos
no monastério de Geshe Wangyal e três anos no Washington Buddhist Vihara. Em
1982<span style='mso-spacerun:yes'>  </span>retornou ao Sri Lanka e em 1984
passou a ser o editor da Buddhist Publication Society em Kandy e à partir de
1988 também se tornou o seu Presidente. Ele regressou aos Estados Unidos em
2002 e desde Julho de 2002 vive e ensina no Bodhi Monastery em New Jersey.<o:p></o:p></span></p>

<p class=Normal>Ele é
autor, tradutor e editor de muitos livros de Budismo Theravada. Entre
as suas várias obras se destacam <i>‘The Discourse on the All-Embracing Net of
Views (1978)’, ‘A Comprehensive Manual of Abhidhamma (1993)’, ‘The Middle
Length Discourses of the Buddha (1995)’</i> e <i>‘The Connected Discourses of the
Buddha (2000)’</i>.<o:p></o:p></span></p>

