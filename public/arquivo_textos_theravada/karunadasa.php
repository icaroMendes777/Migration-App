<p class=Normal>Y. Karunadasa formou-se Bacharel em Artes na University of Ceylon em 1958, obteve um Ph.D na University of London em 1963 e recebeu um título honirário de Doutor em Literatura pela University of Kelaniya em 2002. </p>

