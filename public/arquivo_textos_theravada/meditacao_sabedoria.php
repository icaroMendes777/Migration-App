<p class=Normal>Não é segredo que os seres humanos não respondem de modo inteiramente racional quando se trata de avaliar recompensas. Por exemplo, podemos nos sentir perfeitamente satisfeitos com quanto ganhamos até descobrir o salário do colega no cubículo ao lado.</p>

<p class=Normal>Mas um novo estudo sugere que as pessoas que praticam a meditação Budista com regularidade, na verdade processam essas situações sociais corriqueiras de modo distinto - e os pesquisadores têm as imagens do cérebro para provar isso.</p>

<p class=Normal>Ulrich Kirk e mais um grupo de colaboradores do Baylor Medical College em Houston, Texas, convidaram para o estudo 26 meditadores budistas experientes e 40 voluntários para controle, para participar numa conhecida experiência conhecida como Ultimatum Game. O experimento consiste no seguinte:</p>

<p class=Normal>Uma pessoa tem uma certa quantia de dinheiro para ser dividida com outra pessoa. No experimento as duas pessoas interagem para decidir como dividir o montante em dinheiro. O primeiro jogador propõem como dividir o dinheiro e o segundo pode aceitar ou rejeitar a proposta. Se o segundo jogador rejeitar a proposta por ser demasiado mesquinha - o que acontece com surpreendente freqüência - nenhum deles recebe nada. Se o segundo jogador aceitar a proposta então o dinheiro será dividido de acordo com o que foi proposto.</p>

<p class=Normal>A decisão mais racional é aceitar qualquer oferta que seja proposta, porque obter algo é melhor do que não obter nada, mas o Ultimatum Game sugere que, para muitas pessoas, a emoção ganha da razão. Ser tratado de forma justa é mais importante do que obter um ganho financeiro.</p>

<p class=Normal>Os participantes no jogo tinham que dividir US$ 20 entre si. Quando as ofertas eram exageradamente assimétricas (mantendo US 19 para si mesmo e oferecendo apenas US$ 1 para o outro), 72% dos participantes do grupo de controle recusaram a oferta, significando que ambos participantes saíram de mãos vazias. Mas quando os meditadores jogaram, apenas 46% rejeitaram esse tipo de oferta tão injusta. Mais da metade estavam dispostos a aceitar qualquer coisa que fosse oferecida.</p>

<p class=Normal>Os participantes jogavam o jogo deitados dentro de uma máquina de MRI funcional permitindo que os pesquisadores observassem quais áreas do cérebro eram ativadas ao responderem às ofertas monetárias. Da mesma forma que já havia sido observado em outros experimentos com o Ultimatum Game, a área do cérebro ativada quando os participantes do grupo de controle foram confrontados com uma oferta injusta foi a insula anterior, uma área conectada às emoções de aversão/repulsa.</p>

<p class=Normal>Mas os cérebros dos meditadores reagiram de uma maneira bastante diferente, ativando áreas do cérebro associadas com a representação do estado interior do corpo - "interoception". Na verdade, os pesquisadores encontraram  muita pouca coisa em comum na resposta neurológica dos dois grupos.</p>

<p class=Normal>Kirk, que recrutou os meditadores participantes do estudo no Houston Zen Center e outros grupos Budistas locais, queria explorar um mecanismo para controlar as emoções, diferente daqueles que em geral são estudados na neurociência cognitiva.</p>

<p class=Normal>"Para nós parecia que um modo mais ecológico de fazer isso seria observar os efeitos da atenção plena," ele diz. "A atenção plena, ao contrário do controle das emoções, é o emprego de um ponto de vista externo em relação à própria experiência, ao invés de modificar o seu conteúdo, (por meio da distração), ou o seu contexto, (através da recomposição)."</p>

<p class=Normal>Kirk, que agora é professor pesquisador assistente junto ao Human Neuroimaging Laboratory na Virginia Tech, diz que apesar do comportamento dos meditadores parecer em geral ser mais "racional" do que a maioria do grupo de controle, eles não empregaram o cortex pré-frontal dorso-lateral, a parte do cérebro em geral associada ao raciocínio frio e calculista.</p>

<p class=Normal>Ele também entrevistou informalmente muitos dos meditadores depois deles serem submetidos ao MRI (participarem do jogo). "Eles relataram que as ofertas na verdade não pareciam injustas, ou melhor dizendo, que 'diferença' não equivale a injustiça," diz Kirk. "Foi como se a percepção da diferença provocasse menos reação nos meditadores."</p>

<p class=Normal>Além de Ulrick Kirk, Read Montague, professor de física na Virginia Tech e Jonathan Downar, professor assistente junto à Neuropsychiatry Clinic e o Centre for Addiction and Mental Health na University of Toronto também participaram do estudo.</p>

<p class=Normal>O estudo foi publicado na edição de Abril de 2011 da Frontiers in Decision Neuroscience.</p>



