<p class=Normal>Shravasti Dhammika nasceu na Austrália em 1951 em uma família cristã e se converteu ao budismo com a idade de 18 anos. Em 1973 ele foi para a Tailândia com a intenção de se tornar um monge e de lá para o Laos, Birmânia e, finalmente, para a Índia. Nos três anos seguintes, ele viajou pela Índia para aprender yoga e meditação e, finalmente ordenado sob o Venerável Matiwella Sangharatna, o último discípulo de Anagarika Dharmapala. </p>

<p class=Normal>Em 1976 ele foi para o Sri Lanka, onde estudou Pali no Sri Lanka Vidyalaya e mais tarde se tornou um dos co-fundadores do Centro de Meditação Nilambe e um de seus professores. Desde então, ele tem passado a maior parte de seu tempo no Sri Lanka e em Cingapura. </p>

<p class=Normal>Bhante Dhammika escreveu mais de 25 livros e dezenas de artigos sobre o Budismo e assuntos relacionados. Ele também é conhecido por suas palestras públicas e representou o Budismo Theravada na Conferência Europeia Millennium Budista em Berlim, em 2000. Além da filosofia budista e meditação, Bhante Dhammika tem um profundo interesse na topografia histórica do Budismo e o fenômeno da peregrinação. Ele tem viajado muito pela Índia e outras terras Budistas. Seus outros interesses incluem a história da Índia, arte e botânica. </p>

<p class=Normal>Ele é o autor de <i> Good Question, Good Answer</i> e <i> Buddhism A to Z </i> e também livros sobre peregrinações a sítios Budistas, como <i>Middle Land, Middle Way</i> e <i>Sacred Island</i>. </p>

