<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O <i>Samyutta Nikaya</i>
(SN) é a terceira grande coleção de discursos do Buda que faz parte do Sutta
Pitaka do Cânone em Pali, a compilação dos textos autorizados como a Palavra do
Buda pela escola Theravada de Budismo. No Sutta Pitaka o SN vem depois do <i>Digha
Nikaya</i> e do <i>Majjhima Nikaya</i> e precede o <i>Anguttara Nikaya</i>.
Como os demais Nikayas em Pali, o SN possui correspondentes nas coleções
canônicas das primeiras escolas de Budismo e uma dessas versões foi preservada
no Tipitaka em Chinês onde é conhecido como <i>Tsa-a-han-ching</i>. Esta versão
foi traduzida para o Sânscrito com o título de <i>Samyuktagama</i> e as
evidências indicam ter este pertencido à escola Sarvastivada. Portanto, apesar
do <i>Samyutta Nikaya </i><span style='mso-bidi-font-style:italic'>ter o seu
lugar</span> dentro do Cânone Theravada, não deveríamos nos esquecer que ele
faz parte de um conjunto de textos denominados Nikayas na tradição em Pali, que
predomina no sul da Ásia, e Agamas na tradição Budista do norte da Ásia – e se
situam na origem de toda a herança literária Budista. Foi com base nesses
textos que as primeiras escolas do Budismo estabeleceram os seus sistemas de
doutrina e prática, e mais uma vez, foi para esses textos que as escolas
posteriores de Budismo apelaram ao formular as suas novas visões do caminho
Budista. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Como fonte da doutrina
Budista o SN é particularmente rico, pois nesta coleção são precisamente os
sistemas de classificação doutrinários que servem como base principal para
classificar os discursos do Buda. A palavra <i>samyutta</i> significa
literalmente “jungir,” <i>yutta,</i> possuindo a mesma raiz etimológica que a palavra
“jugo” em Português e, <i>sam,</i> um prefixo que significa “juntos.” Esse
prefixo também aparece nos próprios suttas com o significado doutrinário de
“agrilhoado” ou “atado”. Com esse sentido é um particípio passado relacionado
ao termo técnico <i>samyojana</i>, “grilhão,” sendo que há dez grilhões que
atam os seres vivos ao samsara, o ciclo de renascimentos. Mas a palavra <i>samyutta</i>
também é usada num sentido mais comum significando simplesmente coisas que
estão juntas ou “unidas”, como quando é dito, “Suponham, amigos, um boi negro e
um boi branco que estão unidos por um único arreio ou canga” (SN XXV.232). E
esse é o significado relevante para esta coleção de textos. Estes são suttas,
(discursos), atribuídos ao Buda ou a discípulos eminentes, que estão unidos ou
conectados. E aquilo que os conecta, o “arreio ou canga”, (<i>damena va yottena
va</i>), são os tópicos que dão os títulos a cada capítulo individual, os
samyuttas, nos quais os suttas estão agrupados.<o:p></o:p></span></p>

<p class=Normal><b>As Linhas Mestras do
Samyutta Nikaya</b><o:p></o:p></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Apesar das dimensões imensas
desta obra, o plano de acordo com o qual ela foi construída é bastante simples
e objetivo. O SN que foi preservado na tradição em Pali consta de cinco
principais Vaggas, partes ou “livros.” Esses cinco volumes contêm cinqüenta e
seis samyuttas ou capítulos, cada um se baseando num tema comum. Os samyuttas mais
longos por seu turno estão divididos em sub-capítulos, também chamados vaggas,
enquanto os samyuttas menores pode-se dizer que consistem de um vagga só
idêntico ao próprio samyutta. [<a href="#N1">1</a>] <a name=R1></a>Cada vagga,
idealmente, contém dez suttas, embora na realidade o número de suttas em cada
vagga possa variar de cinco até sessenta. Portanto, encontramos que a palavra
vagga, literalmente “grupo”, é usada para designar tanto as cinco principais
partes da coleção completa (com letra maiúscula, Vagga), como também os
sub-capítulos dentro de cada samyutta, (com letra minúscula, vagga). <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os dois principais
samyuttas, o Khandhasamyutta (22) e o Salayatanasamyutta (35), são tão extensos
que empregam uma unidade divisória adicional para simplificar a organização, o <i>paññasaka</i>
ou “conjunto de cinqüenta.” Esse número é apenas uma aproximação, visto que os
conjuntos em geral contêm um pouco mais que cinqüenta suttas; deveras, o quarto
conjunto do Salayatanasamyutta contém noventa e três suttas, entre eles um
vagga com sessenta! A maioria desses suttas, no entanto, são extremamente
breves, abordando alguns poucos temas com variações. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os cinco Vaggas ou “livros”
principais do SN estão construídos de acordo com princípios distintos. O
primeiro livro, o Sagathavagga, é único por ter sido compilado com base no
gênero literário. Como o nome do Vagga indica, todos os suttas nesta coleção
contêm <i>gathas</i> ou versos, embora nem todos os suttas do SN que contêm
versos façam parte deste Vagga. Em muitos suttas desse Vagga a prosa é reduzida
a um mero suporte para os versos, e no primeiro samyutta até mesmo isso
desaparece de modo que o sutta se transforma num simples intercâmbio de versos,
presumivelmente entre o Buda e um interlocutor. Os outros quatro Vaggas
principais contêm importantes samyuttas que tratam dos principais temas
doutrinários das primeiras escolas do Budismo, seguidos por samyuttas menores
que abrangem uma variedade de tópicos. Cada uma das Partes II, III e IV inicia
com um capítulo extenso dedicado a um tema de suprema importância:
respectivamente, a cadeia de causação (isto é, origem dependente no SN 12), os
cinco agregados (22), e as seis bases internas e bases externas (35). Cada um
desses Vaggas recebe o nome do seu primeiro samyutta e também inclui outros
samyuttas que lidam com outros tópicos importantes, porém secundários em
relação ao tópico principal: na Parte II, os elementos (14); na Parte III,
idéias filosóficas (24); e na Parte IV, sensações (36). Os demais samyuttas, em
cada uma dessas coleções, em geral são menores e com uma temática mais leve,
embora mesmo nesses possamos encontrar textos profundos e poderosos. Na Parte V
todos os temas abordados são de grande importância, isto é, os vários grupos
dos fatores de treinamento que no período pós-canônico foram chamados de os
trinta e sete requisitos ou “asas” para a iluminação, (<i>sattatimsa
bodhipakkhiya dhamma</i>). Este Vagga conclui com um samyutta que trata do
insight original em volta do qual revolve todo o Dhamma, as Quatro Nobres
Verdades. Daí ele ser chamado de Mahavagga, o Grande Livro, embora em algum
momento ele possa ter sido chamado também de Maggavagga, o Livro do Caminho,
(aliás, a versão em Sânscrito traduzida para o Chinês leva esse nome).<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A organização do SN, da
Parte II até a V, pode ser vista como aproximadamente correspondente ao padrão
estabelecido pelas Quatro Nobres Verdades. O<span style="mso-spacerun: yes"> 
</span>Nidanavagga, que foca na origem dependente, revela a gênese causal do
sofrimento, sendo assim uma amplificação da segunda nobre verdade. O<span
style="mso-spacerun: yes">  </span>Khandhavagga e o Salayatanavagga destacam a
primeira nobre verdade, a verdade do sofrimento; pois no seu sentido mais
profundo esta verdade abrange todos os elementos da existência compreendidos
pelos cinco agregados e as seis bases internas e seis bases externas (veja o SN
LVI.13, 14). O Asankhatasamyutta (43), que vem ao final do Salayatanavagga,
discute o incondicionado, um outro termo para a terceira nobre verdade,
Nibbana, a cessação do sofrimento. Por fim, o Mahavagga, trata do caminho da
prática, revela o caminho para a cessação do sofrimento e por conseguinte, a
quarta nobre verdade. Se seguirmos o Samyuktagama Chinês o paralelismo é ainda
mais óbvio pois, esta versão coloca o Khandhavagga no início, depois o Salayatanavagga
que é seguido pelo Nidanavagga, dessa forma criando um paralelo com a primeira
e a segunda verdade na sua seqüência apropriada. Mas esta versão coloca o
Asankhatasamyutta no final do Mahavagga, talvez para mostrar a realização do
incondicionado como o fruto da concretização da prática.<o:p></o:p></span></p>

<p class=Normal><b>O Samyutta Nikaya e o Samyuktagama</b><o:p></o:p></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os comentários em Pali, e
até mesmo o texto canônico Cullavagga, relatam os eventos do Primeiro Concílio
Budista dando a impressão de que os seus participantes, os bhikkhus sêniores,
organizaram o Sutta Pitaka num formato que na sua essência é aquele que vemos
hoje, mesmo no que diz respeito à precisa seqüência dos textos. Isso é
extremamente improvável e é improvável também que o concílio tenha estabelecido
uma revisão final e permanente dos Nikayas. A evidência contrária é
simplesmente enorme. Ela inclui a presença no cânone de suttas que só poderiam
ter surgido depois do Primeiro Concílio (ex: <a href="../sutta/MN84.php">MN 84</a>,
<a href="../sutta/MN108.php">108</a>, <a href="../sutta/MN124.php">124</a>);
indícios de um trabalho de edição dos textos dos próprios suttas; e, um fator
de peso, as diferenças no conteúdo e organização entre os Nikayas em Pali e os
Agamas preservados no Tipitaka em Chinês. É muito mais provável que o que
ocorreu no Primeiro Concílio foi a elaboração de um esquema abrangente para a
classificação dos suttas (preservados apenas na memória dos monges) e a
nomeação de um comitê editorial (talvez vários) para revisar o material
disponível e colocá-lo num formato que contribuísse para facilitar a
memorização e a transmissão oral. Possivelmente também, o comitê editorial, ao
compilar uma coleção de textos autorizados, teria considerado cuidadosamente o
propósito ao qual as coleções deveriam servir e então, teria formulado as
diretrizes para a sua classificação de modo que satisfizesse esses propósitos.
Este é um ponto ao qual retornarei mais abaixo. A distribuição dos textos entre
grupos de recitadores, (<i>bhanakas</i>), encarregados da tarefa de
preservá-los e transmití-los para a posteridade, ajudaria a explicar as
diferenças entre as distintas revisões, bem como a ocorrência dos mesmos suttas
em diferentes Nikayas. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A comparação do SN em Pali
com o Samyuktagama em Chinês é particularmente instrutiva e revela uma
correspondência extraordinária entre os seus conteúdos organizados de uma forma
distinta. Eu já fiz uma alusão acima a algumas diferenças de organização, mas é
esclarecedor examiná-las mais detalhadamente. A versão em Chinês contém nove
Vaggas principais. O primeiro é o Khandhavagga, (em Pali é o III), o segundo é
o Salayatanavagga, (em Pali, o IV), o terceiro o Nidanavagga, (em Pali, o II),
que também contém o Saccasamyutta (56) e o Vedanasamyutta (36), desviando-se
significativamente do SN nessas alocações. Depois segue o quarto chamado
Savakavagga, sem uma contraparte na versão em Pali mas que inclui entre outros
os Sariputta- (28), Moggallana- (40), Lakkhana- (19), Anuruddha- (52), e
Cittasamyuttas (41). O quinto, cujo título em Pali seria Maggavagga,
corresponde ao SN Mahavagga (em Pali é o V também), mas os samyuttas, ali,
estão organizados numa seqüência que segue mais de perto a seqüência canônica
que constitui os trinta e sete requisitos ou “asas” para a iluminação:
Satipatthana (47), Indriya (48), Bala (50), Bojjhanga (46), e Magga (45); esta
parte também inclui os Anapanasati- (54) e Sotapattisamyuttas (55), enquanto
uma série de capítulos ao final inclui um Jhanasamyutta (53) e um
Asankhatasamyutta (43). O sexto Vagga do Samyuktagama não tem um paralelo em
Pali, mas contém o Opammasamyutta (20) e uma coleção de suttas sobre pessoas
enfermas que reúne textos distribuídos ao longo de vários capítulos do SN.
Então, como sétimo livro vem o Sagathavagga (em Pali, o I), com doze samyuttas
– onze da versão em Pali, mas numa ordem distinta e com a adição do
Bhikkhusamyutta (21), que na coleção em Pali contém apenas suttas em versos. Por
fim, vem um Buddha- ou Tathagatavagga, que inclui os Kassapa- (16) e
Gamanisamyuttas (42), e um Assasamyutta, “Discursos Agrupados sobre Cavalos.”
Este último capítulo inclui suttas que no Cânone em Pali são encontrados no
Anguttara Nikaya.<o:p></o:p></span></p>

<p class=Normal><b><a name=nikayas> </a>O
Papel dos Quatro Nikayas</b><o:p></o:p></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A opinião acadêmica
predominante, fomentada pelos próprios textos, afirma que a principal base para
distinguir os quatro Nikayas é o tamanho dos suttas. Assim os suttas mais
extensos estão contidos no <a href="../sutta/digha_nikaya.php">Digha Nikaya</a>,
aqueles com extensão média no <a href="../sutta/majjhima_nikaya.php">Majjhima Nikaya</a>
e os discursos mais curtos estão distribuídos entre o <a
href="../sutta/samyutta_nikaya.php">Samyutta</a> e o <a href="../sutta/anguttara_nikaya.php">Anguttara
Nikaya</a>, sendo que o primeiro classifica os suttas por tema e o último pelo
número de itens abordados na exposição. No entanto, num importante estudo
pioneiro, a erudita em Pali, Joy Manné, questionou a premissa de que só o
tamanho explica as diferenças entre os Nikayas. Ao comparar cuidadosamente os
suttas do DN com aqueles do MN, Manné concluiu que as duas coleções têm a
intenção de atender dois propósitos distintos dentro da revelação do Buda. Na
opinião dela, o DN estaria primordialmente voltado para o propósito de
propaganda, para atrair conversos para a nova religião e dessa forma estaria
destinado principalmente para não Budistas que possuem uma inclinação favorável
em relação ao Budismo; o MN, em comparação, estaria direcionado para o interior
da comunidade Budista e o seu propósito seria exaltar o Mestre (tanto como uma
pessoa real, bem como um arquétipo) e de integrar os monges na comunidade e na
prática. Manné também propõe que “cada um dos quatro primeiros Nikayas surgiu
para satisfazer uma necessidade e propósito distintos para o crescimento e
desenvolvimento da comunidade Budista” (p. 73). Aqui, iremos tentar responder
rapidamente a questão referente ao propósito que estaria por detrás da
compilação do SN e do AN, em comparação com os outros dois Nikayas.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ao abordar essa questão
deveríamos primeiro observar que os suttas desses dois Nikayas proporcionam
apenas um mínimo de informação circunstancial. Com raras exceções, na verdade,
um ambiente está totalmente ausente com o cenário indicando simplesmente que o
sutta foi proferido pelo Abençoado em tal e qual lugar. Deste modo, enquanto o
DN e o MN estão repletos de drama, debate e narrativa, especialmente o DN, com
uma abundância de excursões imaginativas, aqui, o entorno decorativo está
totalmente ausente. No SN todo o entorno é reduzido a uma única sentença, em
geral abreviada para “Em Savathi, no Bosque de Jeta”, e no quarto livro até mesmo
isso desaparece. Exceto o Sagathavagga, que se situa numa categoria à parte, os
outros quatro livros do SN possuem pouca ornamentação. Os suttas em si, em
geral, são formulados como proclamações doutrinárias pelo próprio Buda; algumas
vezes eles assumem a forma de uma consulta com o Mestre por um único bhikkhu ou
um grupo de bhikkhus; outras vezes estão formulados como uma discussão entre
dois monges eminentes. Muitos suttas consistem em um pouco mais do que algumas
poucas sentenças e não é incomum eles elaborarem permutações sobre um único
tema. Quando chegamos na Parte V, cadeias completas de suttas são reduzidas a
meras palavras em versos mnemônicos, ficando por conta do recitador (ou do
mestre contemporâneo) a tarefa de expandir o esboço e completar o conteúdo.
Isso indica que os suttas do SN (tal qual o AN) não seriam, como regra geral,
destinados às pessoas de fora ou mesmo àqueles recém-conversos, mas
direcionados àqueles que já haviam buscado refúgio no Dhamma e se encontravam
profundamente imersos no seu estudo e prática. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Com base no seu arranjo
temático, podemos postular que, por suas características mais distintivas como
uma coleção, (embora, com certeza não em todos os particulares), o SN foi
compilado para servir como o repositório dos suttas muito curtos e concisos que
revelam os insights radicais do Buda quanto à natureza da realidade e o seu
caminho único para a emancipação espiritual. Esta coleção teria atendido às
necessidades de dois tipos de discípulos dentro da comunidade monástica. Os
especialistas doutrinários, aqueles monges e monjas que eram capazes de
compreender as dimensões mais profundas da sabedoria e que tomaram para si a
tarefa de esclarecer para os outros os muitos suttas com nuances abstrusas,
profundas e delicadas, relacionados a tópicos de peso como a origem dependente,
os cinco agregados, as seis bases sensuais, os fatores do caminho e as Quatro
Nobres Verdades. Esta coleção seria perfeitamente adequada para aqueles
discípulos com inclinação intelectual, que se deliciariam com a exploração das
profundas implicações do Dhamma e com o trabalho de explicá-las para os
companheiros no caminho espiritual. O segundo tipo de discípulos, aos quais o
SN parece ter sido designado, eram aqueles monges e monjas que já haviam
realizado os estágios preliminares do treinamento meditativo e que tinham a
intenção de aperfeiçoar os seus esforços com a realização direta da verdade
suprema. Como os suttas nesta coleção têm uma importância vital para os
meditadores inclinados a realizar o “conhecimento das coisas como elas na
verdade são,” eles poderiam muito bem ter constituído o corpo principal de um
currículo de estudo compilado para guiar os meditadores na prática do insight. </span><o:p></o:p></p>

