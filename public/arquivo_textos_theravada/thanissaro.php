<p class=Normal style='margin-right:.5in;mso-margin-top-alt:auto;mso-margin-bottom-alt:auto;margin-left:.75in'>
 A prática de meditação consiste na mera observação do fluxo dos fenômenos mentais e corporais, sem qualquer tipo de interferência?<o:p></o:p></span></p>


