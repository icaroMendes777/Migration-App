<p class=Normal style='margin-right:.5in;mso-margin-top-alt:auto;mso-margin-bottom-alt:auto;margin-left:.75in'>
Nenhuma outra qualidade interior é tão importante na elucidação do problema do sofrimento quanto a atenção com 
sabedoria, <i>yoniso-manasikara</i>.<o:p></o:p></span></p>

