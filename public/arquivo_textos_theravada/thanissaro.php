<p class=Normal style='margin-right:.5in;mso-margin-top-alt:auto;mso-margin-bottom-alt:auto;margin-left:.75in'>O samsara é um lugar ou é o processo através do qual criamos e recriamos
o nosso próprio sofrimento? Neste ensaio o autor compara samsara a um vício que
pode ser abandonado – ou não. Qual será a sua escolha?<o:p></o:p></span></p>
<p class=Normal>&nbsp;</p>
<ul style='margin-top:0in' type=disc>
 <li class=Normal style='mso-list:l34 level1 lfo2;tab-stops:list .5in'><span
     lang=PT-BR style='mso-bidi-font-size:10.0pt;font-family:
     ;mso-ansi-language:PT-BR'><b><a href="medo.php">Libertação do Medo</a></span></b> (2002; 19k/6pág.) <o:p></o:p></span></li>
</ul>

<p class=Normal style='margin-right:.5in;mso-margin-top-alt:auto;mso-margin-bottom-alt:auto;margin-left:.75in'>Desde o início do tempo os seres humanos têm vivido sob o medo: o medo
da mudança, da perda, da morte. Infelizmente, como temos testemunhado no
cenário mundial, as pessoas aprenderam um sem número de formas inábeis sobre
como lidar com o medo. O autor escreve: “A resposta mais inábil ao medo é
quando, percebendo que a nossa vida ou posses estão em perigo, acreditamos que
podemos obter força e segurança destruindo as vidas e posses dos outros.” Neste
ensaio o autor descreve a prescrição do Buda para superar o medo de uma vez por
todas, eliminando-o pela raiz.<o:p></o:p></span></p>
<p class=Normal>&nbsp;</p>
<ul style='margin-top:0in' type=disc>
 <li class=Normal style='mso-list:l34 level1 lfo2;tab-stops:list .5in'><span
     lang=PT-BR style='mso-bidi-font-size:10.0pt;font-family:
     ;mso-ansi-language:PT-BR'><b><a href="des_percepcao.php">Des-percepção</a></span></b> (2002; 22k/5pág.) <o:p></o:p></span></li>
</ul>

<p class=Normal style='margin-right:.5in;mso-margin-top-alt:auto;mso-margin-bottom-alt:auto;margin-left:.75in'>
     Quando sentamos para meditar em geral trazemos conosco um número de
     suposições acerca do que são as nossas percepções, como é e como deveria
     ser a nossa experiência de meditação. Como meditadores a nossa tarefa é
     aprender a formular as perguntas corretas – perguntas que nos ajudem a
     romper os estratos dessas noções falsas pré-concebidas. Este artigo,
     baseado em uma palestra do Dhamma, contém conselhos práticos para
     meditadores de todos os níveis.<o:p></o:p></span></p>
<p class=Normal>&nbsp;</p>
<ul style='margin-top:0in' type=disc>
 <li class=Normal style='mso-list:l34 level1 lfo2;tab-stops:list .5in'><span
