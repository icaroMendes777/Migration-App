<p class=Normal style='margin-right:.5in;mso-margin-top-alt:auto;mso-margin-bottom-alt:auto;margin-left:.75in'>
A fé é necessária? Neste ensaio o autor questiona três mal-entendidos comuns sobre o papel da fé no Budismo. <o:p></o:p></span></p>


