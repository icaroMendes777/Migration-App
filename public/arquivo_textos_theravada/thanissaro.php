<p class=Tit1 align=center style='text-align:center'>Ajaan Thanissaro (Thanissaro Bhikkhu)</Tit1>

<p class=Normal>Ajaan Thanissaro (Geoffrey DeGraff) é um monge Budista Americano nascido em 1949. Depois de se formar no Oberlin College com um 
diploma em História Intelectual da Europa, ele começou a praticar meditação com o seu mestre, Ajaan Fuang Jotiko - 
um membro da tradição de florestas da Tailândia e discípulo de Ajaan Lee - em 1974, tendo se ordenado como 
monge em 1976, continuando a estudar com Ajaan Fuang até a sua morte em 1986. Em 1991 ele foi para o sul da 
California para ajudar no estabelecimento do Monastério de Floresta Metta nas montanhas do norte do condado de 
San Diego. Ele se tornou o abade do monastério em 1993 e foi formalmente nomeado preceptor em 1995. Ele regularmente 
ensina no John Wayne Dhamma Center no condado de Orange e também faz parte do corpo de professores visitantes do BCBS – Barre Center of Buddhist Studies em Barre/MA, EUA.<o:p></o:p></span></p>

<p class=Normal>Ajaan Geoff, como é conhecido pelos seus alunos, é o autor de <i> The Mind Like Fire Unbound, The Buddhist Monastic Code</i> e <i>The Wings to Awakening</i>. Ele também traduziu vários guias de meditação de mestres Tailandeses, incluindo as obras completas de Ajaan Lee Dhammadharo que foi o mestre de Ajaan Fuang.<o:p></o:p></span></p>

