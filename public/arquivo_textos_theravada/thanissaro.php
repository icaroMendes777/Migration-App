<p class=Normal style='margin-right:.5in;mso-margin-top-alt:auto;mso-margin-bottom-alt:auto;margin-left:.75in'>Ao longo dos anos, a palavra <i> sati</i> tem acumulado uma série de significados que vão muito além da definição original do Buda. A prática da meditação exige uma compreensão clara do que é <i>sati</i> e como usá-la com habilidade em conjunto com outras qualidades mentais.    <o:p></o:p></span></p>

