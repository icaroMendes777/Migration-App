<p class=Normal> 
Agora determine que
a sua mente ouça o Dhamma. Durante o período em que eu estiver falando, preste
atenção às minhas palavras como se o próprio Buda estivesse sentado à sua
frente. Feche os olhos e sente-se confortavelmente, componha a sua mente e faça
com que ela foque a atenção em um só ponto. Com humildade permita que a Jóia
Tríplice da sabedoria, verdade e pureza encontre um lugar no seu coração como
forma de demonstrar o seu respeito pelo Iluminado. <o:p></o:p></span></p>

<p class=Normal>
Hoje eu não
trouxe nada material, com substância para oferecer-lhe, somente o Dhamma, os
ensinamentos do Buda. Ouça bem. Você precisa entender que mesmo o próprio Buda,
com o seu grande depósito de virtude acumulada, não pôde evitar a morte física.
Quando ele alcançou a velhice, ele abandonou o seu corpo e soltou o seu pesado
fardo. Agora você também precisa aprender a estar satisfeita com os muitos anos
em que esteve sujeita a esse corpo. Você deveria sentir que já chega. <o:p></o:p></span></p>

<p class=Normal>
Você pode
compará-lo a utensílios de cozinha que teve por muito tempo - suas xícaras,
pires, pratos<span style="mso-spacerun: yes">  </span>e assim por diante.
Quando você os comprou eles estavam limpos e brilhantes mas agora após usá-los
por tanto tempo, eles mostram os sinais do tempo. Alguns já estão quebrados,
outros desapareceram e aqueles que sobraram estão se deteriorando; eles não possuem
uma forma estável, e faz parte da sua natureza que seja assim. Com o corpo é o
mesmo - esteve continuamente mudando desde o dia em que você nasceu, através da
infância e adolescência, até agora quando alcançou a velhice. Você precisa
aceitar isso. O Buda disse que os fenômenos, <i style='mso-bidi-font-style:normal'>(sankharas),</i>
quer sejam internos, do corpo, ou externos, são desprovidos de um eu, a mudança faz parte
da natureza deles. Contemple essa verdade até que você a veja
claramente. <o:p></o:p></span></p>

<p class=Normal>
Esse mesmo pedaço
de carne que aqui está deitado em decadência é <i>saccadhamma</i>, a verdade. A
verdade deste corpo é <i>saccadhamma</i>, e esse é o ensinamento imutável do Buda. O
Buda nos ensinou a olhar para o corpo, contemplá-lo e aceitar a sua natureza.
Precisamos estar em paz com o corpo, não importando o estado em que ele esteja.
O Buda ensinou que devemos nos assegurar que somente o corpo esteja
aprisionado e não permitir que a mente seja aprisionada junto. Agora, à
medida que o seu corpo começa a perder vitalidade e deteriorar pela idade, não
resista a isso, mas não permita que a sua mente se deteriore junto com ele.
Mantenha a mente separada. Energize a sua mente compreendendo a verdade de como
as coisas são. O Buda ensinou que essa é a natureza do corpo, não pode ser de
outra forma: tendo nascido, ele envelhece e se enferma e depois morre. Essa é
uma grande verdade com a qual você está se deparando agora. Olhe para o corpo
com sabedoria e entenda isso. <o:p></o:p></span></p>

<p class=Normal>
Se a sua casa
estiver inundada ou completamente queimada, qualquer que seja o perigo que a
ameace, faça com que seja somente com a casa. Se ocorrer uma enchente, não
permita que ela inunde a sua mente. Se ocorrer um incêndio, não permita que ele
queime o seu coração. Deixe que seja somente a casa, aquilo que é externo a
você,<span style="mso-spacerun: yes">  </span>fique inundada e queimada. Permita
que a mente se liberte dos seus apegos. Este é o momento certo. <o:p></o:p></span></p>

<p class=Normal>
Você já está viva
há muito tempo. Os seus olhos já viram inumeráveis formas e cores, os seus
ouvidos ouviram tantos sons, você teve incontáveis experiências. E isso é tudo
que elas foram - somente experiências. Você comeu comidas deliciosas, e todos
os sabores deliciosos foram somente sabores deliciosos, nada mais. Os sabores
desagradáveis foram somente sabores desagradáveis, isso é tudo. Se o olho vê
uma forma bonita, isso é tudo, somente uma forma bonita. Uma forma feia é
somente uma forma feia. O ouvido ouve um som que hipnotiza, melodioso e não é
nada além disso. Um som áspero, sem harmonia é somente isso. <o:p></o:p></span></p>

<p class=Normal>
O Buda disse que
pobre ou rico, jovem ou velho, humano ou animal, nenhum ser neste mundo pode
manter a si mesmo em um mesmo estado por muito tempo, tudo experimenta mudança.
Esse é um fato da vida para o qual não há remédio. Mas o Buda disse que o que
podemos fazer é contemplar o corpo e a mente de tal forma a ver a sua
impersonalidade, ver que nenhum deles é &quot;eu&quot; ou &quot;meu&quot;. Eles
possuem meramente uma realidade provisória. É como esta casa: ela é sua somente
de forma nominal, você não a pode levar para nenhum lugar. Ocorre o mesmo com a
sua fortuna, as suas posses e a sua família - elas são todas suas somente no
nome, na verdade elas não lhe pertencem, elas pertencem à natureza. Agora essa
verdade não se aplica somente a você, todos estão na mesma posição, mesmo o
Buda e os seus discípulos iluminados. Eles diferem de nós apenas num aspecto,
na aceitação das coisas do modo como elas são, eles viram que não pode ser de
outro modo. <o:p></o:p></span></p>

<p class=Normal>
Portanto o Buda
nos ensinou a mapear e examinar este corpo, das solas dos pés até o topo da
cabeça e em seguida até as solas dos pés novamente. Dê uma olhada no corpo. Que
tipo de coisas você vê? Existe algo que seja intrinsecamente limpo? Você pode
encontrar alguma essência permanente? Todo este corpo está constantemente se
degenerando e o Buda nos ensinou a ver que ele não nos pertence. Faz parte da
natureza que o corpo seja assim porque todos os fenômenos condicionados estão
sujeitos à mudança. De que outra forma poderia ser? Na verdade, não existe nada
de errado com o corpo. Não é o corpo que faz com que você sofra, mas sim a sua
forma de pensar incorreta. Quando você vê o certo de maneira errada,
inevitavelmente haverá confusão. É como a água de um rio. Ela naturalmente flui
quando há uma inclinação, ela nunca flui em sentido contrário, assim é a
natureza. Se uma pessoa estivesse às margens de um rio e vendo a correnteza
fluindo rapidamente, tolamente desejasse que as águas fluíssem em sentido
contrário, ela iría sofrer. O que quer que ela estivesse fazendo, a sua maneira
errada de pensar não lhe daria paz. Ela ficaria infeliz devido ao seu entendimento
incorreto, pensando contra a correnteza. Se ela tivesse o entendimento correto
ela veria que a água deve inevitavelmente fluir de acordo com a inclinação, e
até que ela entenda e aceite esse fato, a pessoa ficará agitada e preocupada. <o:p></o:p></span></p>

<p class=Normal>
O rio que flui de
acordo com a inclinação é igual ao seu corpo. Tendo sido jovem seu corpo
envelheceu e agora está fluindo tranqüilamente para a morte. Não fique
desejando que seja diferente, não é algo que você tem o poder de remediar. O
Buda nos disse para ver as coisas como elas são e então soltar o nosso apego em
relação a elas. Tome essa noção de abandono como o seu refúgio. <o:p></o:p></span></p>

<p class=Normal>
Sustente a
meditação, mesmo que você se sinta cansada e exausta. Deixe que a sua mente
permaneça com a respiração. Respire fundo algumas vezes e depois fixe a mente
na respiração usando a palavra &quot;Buddho&quot; como mantra. Faça disso uma
prática rotineira. Quanto mais exausta você estiver, mais sutil e focalizada
deve ser a sua concentração, assim você poderá suportar as sensações dolorosas que
surgem. Quando você começar a se sentir cansada, faça com que todo o seu
pensamento pare, que a mente se recupere e se volte para conhecer a respiração.
Mantenha a recitação interior: &quot;Bud-dho, Bud-dho&quot;. Solte tudo que é
externo. Não se apegue a pensamentos acerca dos seus filhos e parentes, não se
apegue a absolutamente nada. Solte. Deixe que a mente se unifique em um só
ponto e permita que essa mente acalmada permaneça com a respiração. Faça com
que a respiração seja o seu único objeto de interesse. Concentre até que a
mente se torne cada vez mais sutil, até que as sensações sejam insignificantes
e que haja uma grande clareza e vivacidade interiores. Então quando surgirem
sensações dolorosas elas irão gradualmente ceder por si mesmas. Finalmente,
você irá olhar para a respiração como se fosse um parente que a tivesse vindo
visitar. <o:p></o:p></span></p>

<p class=Normal>
Quando um parente
vai embora, nós o acompanhamos até a porta. Esperamos até que já não o possamos
ver mais e então voltamos para dentro. Observamos a respiração da mesma forma.
Se a respiração é grosseira, nós sabemos que ela é grosseira. Se ela é sutil,
sabemos que é sutil. À medida que ela vai se tornando cada vez mais discreta
nós a acompanhamos, simultaneamente despertando a mente. Eventualmente a
respiração desaparece por completo e tudo o que resta é a sensação de estar
alerta. A isto se denomina encontrar o Buda. Temos aquele entendimento
cristalino que é chamado de &quot;Buddho&quot;, aquele que sabe, aquele que está
desperto, aquele que irradia. É encontrar e permanecer com o Buda, com
conhecimento e clareza. Pois foi somente o Buda histórico de carne e osso que
realizou o <i style='mso-bidi-font-style:normal'>parinibbana;</i> o verdadeiro
Buda, o Buda que é o conhecimento claro e luminoso, nós podemos experimentar e
realizar mesmo hoje e quando assim fizermos, o coração é um só. <o:p></o:p></span></p>

<p class=Normal>
Portanto, solte,
deixe tudo de lado, tudo exceto o conhecimento. Não se iluda se visões ou sons
surgirem na sua mente durante a meditação. Deixe-os de lado. Não se apegue a
absolutamente nada. Permaneça apenas com essa atenção não dual. Não se preocupe
com o passado ou o futuro, fique tranqüila e você irá alcançar o lugar onde não
existe progresso, recuo e nem parada, onde não existe nada para se agarrar ou
apegar. Porque? Porque não existe &quot;eu&quot; ou
&quot;meu&quot;. Tudo se foi. O Buda nos ensinou a que fiquemos vazios desse modo,
a não carregar nada conosco. Conhecendo e tendo conhecido, solte. <o:p></o:p></span></p>

<p class=Normal>
Realizar o
Dhamma, o caminho da libertação do ciclo de nascimento e morte, é uma tarefa
que todos temos que realizar sozinhos. Portanto, continue tentando soltar tudo
e entender os ensinamentos. Coloque esforço real na sua contemplação. Não se
preocupe com a sua família. No momento eles são o que são, no futuro serão como
você. Não existe ninguém neste mundo que possa escapar a esse destino. O Buda
nos disse para deixar de lado tudo aquilo que não possui uma essência
verdadeira, permanente. Se você colocar tudo de lado, irá ver a verdade, se
você não fizer isso, não irá vê-la. Assim é como é, igual para todos, portanto
não se preocupe e não se agarre a nada. <o:p></o:p></span></p>

<p class=Normal>
Mesmo que você se
dê conta de que está pensando, não há problema, contanto que você pense
sabiamente. Não pense de maneira tola. Se você pensar nos seus filhos, pense
neles com sabedoria, não de forma tola.<span style="mso-spacerun: yes"> 
</span>Qualquer coisa que chame a atenção da mente, pense e entenda aquela
coisa com sabedoria, consciente da sua natureza. Se você entende alguma coisa
com sabedoria, então você a solta e não há sofrimento. A mente estará
brilhante, feliz e em paz, e evitando distrações ela estará unificada. Neste
momento o que lhe pode ajudar e apoiar é a sua respiração. <o:p></o:p></span></p>

<p class=Normal>
Essa é uma tarefa
sua, de ninguém mais. Deixe que os outros façam as tarefas deles. Você tem a
sua própria tarefa e responsabilidade e você não precisa assumir aquelas da sua
família. Não assuma nada mais, solte tudo. Esse ato de soltar irá acalmar a sua
mente. A sua única responsabilidade agora é de focar a sua mente e fazer com
que ela fique em paz. Deixe todo o demais para os outros. Formas, sons, odores,
sabores - deixe que os outros tomem conta disso. Deixe tudo isso de lado e faça
o seu próprio trabalho, cumpra a sua responsabilidade. O que quer que surja na
sua mente, seja medo ou dor, medo da morte, ansiedade pelos outros ou o que
seja, diga-lhes: &quot;Não me perturbem. Vocês não são mais minha
responsabilidade&quot;. Apenas diga isso para si mesma quando você vir esses <i
style='mso-bidi-font-style:normal'>dhammas </i>surgindo.<o:p></o:p></span></p>

<p class=Normal>
A que se refere a
palavra &quot;<i style='mso-bidi-font-style:normal'>dhamma</i>&quot; ? Tudo é
dhamma. Não existe nada que não seja dhamma. E o que seria &quot;mundo&quot; ?
O mundo é exatamente o estado mental que a está deixando agitada neste mesmo
momento. &quot;O que essa pessoa irá fazer? O que aquela pessoa irá fazer?
Quando eu estiver morta quem irá cuidar deles? Como eles irão se
arranjar?&quot; Isso tudo é &quot;o mundo&quot;. Mesmo o mero surgimento de um
pensamento de medo ou dor, é o mundo.<o:p></o:p></span></p>

<p class=Normal>
Jogue o mundo
fora! O mundo é assim mesmo. Se você permitir que ele surja na mente e domine a
consciência então a mente se tornará nebulosa e não poderá ver a si mesma.
Portanto, para tudo que surgir na mente, simplesmente diga: &quot;Não diz
respeito a mim. É impermanente, insatisfatório e não-eu.&quot; <o:p></o:p></span></p>

<p class=Normal>
Pensar que você
gostaria de viver por muito tempo a fará sofrer. Mas pensar que você gostaria
de morrer logo ou muito rapidamente também não é correto, é sofrimento não é?
As condições não nos pertencem, elas seguem as suas próprias leis da natureza.
Você não pode fazer nada acerca do modo como o corpo é. Você pode embelezá-lo
um pouco, fazer com que seja atraente e limpo durante algum tempo, tal como as
garotas que passam batom e deixam as unhas crescer. Porém, quando a velhice
chega, todos estão no mesmo barco. O corpo é assim, você não pode fazer com que
seja de outro modo. Mas o que você pode melhorar e embelezar é a mente. <o:p></o:p></span></p>

<p class=Normal>
Qualquer pessoa
pode construir uma casa de madeira e tijolos mas o Buda ensinou que esse tipo
de casa não é o nosso verdadeiro lar, é nosso só nominalmente. É uma casa no
mundo e segue as regras do mundo. O nosso verdadeiro lar é a paz interior. Uma
casa pode muito bem ser bonita mas não tem muita paz. Existe esta preocupação e
depois aquela, esta ansiedade e depois aquela. Portanto dizemos que ela não é o
nosso verdadeiro lar, está fora de nós, cedo ou tarde vamos ter que abrir mão
dela. Não é um lugar em que podemos viver permanentemente porque na verdade não
nos pertence, é parte do mundo. Com o nosso corpo ocorre o mesmo; assumimos que
ele seja parte do eu, que seja &quot;eu&quot; e &quot;meu&quot;, mas na
verdade não é nada disso, é uma outra casa do mundo. O seu corpo seguiu o seu
curso natural do nascimento até agora, está velho e enfermo e você não pode
proibi-lo disso, assim é como é. Querer que seja diferente é tão tolo quanto
querer que um pato seja uma galinha. Quando você vê que isso é impossível, que
um pato tem que ser um pato, e que uma galinha tem que ser uma galinha, que os
corpos têm que envelhecer e morrer, você terá energia e força. Não importa
quanto você queira que o corpo se mantenha e dure por muito tempo, isso não irá
ocorrer. <o:p></o:p></span></p>

