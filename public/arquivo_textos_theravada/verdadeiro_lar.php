Ou você poderia somente inspirar? Queremos que as coisas sejam permanentes, mas
elas não podem ser, é impossível. Uma vez que tenhamos inspirado, é necessário
expirar, quando expiramos é necessário inspirar outra vez, e assim é a
natureza, não é? Tendo nascido, envelhecemos e ficamos enfermos e depois
morremos e isso é perfeitamente natural e normal. Porque os sankharas fizeram a
