raça humana ainda está aqui hoje. <o:p></o:p></span></p>

<p class=Normal>
Assim que
nascemos, estamos mortos. Nosso nascimento e morte são somente uma coisa. Tal
como uma árvore: quando há uma raiz tem que haver galhos. Quando há galhos tem
que haver uma raiz. Não se pode ter uma sem a outra. É um pouco engraçado ver
como face à morte as pessoas ficam tão angustiadas e distraídas, chorosas e
tristes e no nascimento tão felizes e contentes. É a delusão, ninguém nunca vê
isso com clareza. Eu penso que se você realmente quer chorar, então seria
melhor fazê-lo quando alguém nasce. Pois na verdade o nascimento é morte, morte
é nascimento, a raiz é o galho, o galho a raiz. Se você precisa chorar, chore
pela raiz, chore pelo nascimento. Veja com atenção: se não houvesse nascimento
não haveria morte. Você pode entender isso? <o:p></o:p></span></p>

<p class=Normal>
Não pense muito.
Somente pense: &quot;Assim é como as coisas são&quot;. É a sua tarefa, a sua
responsabilidade. Neste exato momento ninguém pode ajudá-la, não há nada que a
sua família e as suas posses possam fazer por você. A única coisa que lhe pode
ajudar é o entendimento correto. <o:p></o:p></span></p>

<p class=Normal>
Portanto não
vacile. Solte tudo. Jogue tudo fora. <o:p></o:p></span></p>

<p class=Normal style='text-align:justify'> Mesmo que você não queira soltar, tudo está indo
embora de qualquer jeito. Você pode ver como todas as diferentes partes do seu
corpo estão tentando escapar? Tome o seu cabelo: quando você era jovem ele era
grosso e preto, agora ele está caindo. Está indo embora. Os seus olhos
costumavam ser bons e fortes e agora eles estão fracos, a sua vista embaçada.
Quando os órgãos já não agüentam mais, eles vão embora, esta não é a casa
deles. Quando você era uma criança os seus dentes eram sadios e firmes agora
eles estão balançando, talvez você tenha dentadura. Os seus olhos, ouvidos,
nariz, língua - tudo está tentando ir embora porque esta não é a casa deles.
Você não pode fazer uma morada permanente em um sankhara, você pode permanecer
por pouco tempo e depois tem que partir. O mesmo que um inquilino observando a
sua pequena casa com os olhos enfraquecidos. Os seus dentes já não estão bem,
os seus ouvidos já não estão bem, o seu corpo já não está saudável, todos estão
partindo. <o:p></o:p></span></p>

<p class=Normal>
Portanto você não
precisa se preocupar com nada, porque este não é o seu verdadeiro lar, é
somente um abrigo temporário. Tendo vindo a este mundo, você deveria contemplar
a sua natureza. Tudo o que existe está se preparando para desaparecer. Olhe
para o seu corpo. Existe algo que ainda se encontre na sua forma original? A sua
pele está como costumava estar? O seu cabelo? Não é o mesmo, é? Para onde foi
tudo? Assim é a natureza, como as coisas são. Quando o tempo chega, as
formações seguem o seu próprio curso. Não há nada neste mundo com o qual se
possa contar - é um ciclo interminável de perturbações e problemas, prazeres e
dores. Não há paz. <o:p></o:p></span></p>

<p class=Normal>
Quando não temos
um verdadeiro lar somos como um viajante sem rumo, indo em uma direção por
algum tempo e depois para outra direção, parando por algum tempo para depois
continuar outra vez. Até que regressemos ao nosso verdadeiro lar nos sentimos
desconfortáveis com o que quer que estejamos fazendo, da mesma forma como uma
pessoa que saiu do seu vilarejo para uma viagem. Somente quando regressa outra
vez para casa é que ela pode realmente relaxar e estar em paz. <o:p></o:p></span></p>

<p class=Normal>
Em nenhum lugar
do mundo pode ser encontrada a paz verdadeira. Os pobres não têm paz e nem os
ricos. Os adultos não têm paz, as crianças não têm paz, aqueles com pouca
escolaridade não têm paz e nem os que têm muito estudo. Não existe paz em lugar
nenhum. Essa é a natureza do mundo. <o:p></o:p></span></p>

<p class=Normal>
Aqueles que
possuem poucas posses sofrem e da mesma forma aqueles que possuem muitas.
Crianças, adultos, velhos, todos sofrem. O sofrimento de ser velho, o
sofrimento de ser jovem, o sofrimento de ser rico e o sofrimento de ser pobre -
nada além de sofrimento. <o:p></o:p></span></p>

<p class=Normal>
Quando você
contempla as coisas dessa forma você vê <i style='mso-bidi-font-style:normal'>anicca,</i>
impermanência e <i style='mso-bidi-font-style:normal'>dukkha,</i> insatisfação.
Porque as coisas são impermanentes e não trazem satisfação? É porque são <i
style='mso-bidi-font-style:normal'>anatta,</i> não-eu. <o:p></o:p></span></p>

<p class=Normal>
Ambos, o seu
corpo que está aqui deitado enfermo e dolorido e a mente que está consciente da
sua enfermidade e dor, são denominados dhammas. Aquilo que é desprovido de
forma, os pensamentos, sensações e percepções são denominados <i
style='mso-bidi-font-style:normal'>namadhamma.</i> Aquilo que está atormentado
com dores e desconfortos é denominado <i style='mso-bidi-font-style:normal'>rupadhamma.</i>
O material é dhamma e o não material é dhamma. Portanto vivemos com dhammas, no
dhamma, nós somos dhamma. Na verdade não existe um eu que pode ser encontrado
em nenhum lugar, só existem dhammas que estão continuamente surgindo e
desaparecendo, que é a sua natureza. A cada momento estamos experimentando o
nascimento e a morte. Assim é como as coisas são. <o:p></o:p></span></p>

<p class=Normal>
Quando pensamos
no Buda, da forma tão verdadeira como ele falou, sentimos o quanto ele é digno
de que o saudemos, honremos e respeitemos. Sempre que vemos a verdade de algo,
vemos os seus ensinamentos, mesmo sem nunca, na verdade termos praticado o
Dhamma. Porém mesmo que tenhamos conhecimento dos seus ensinamentos, que os
tenhamos estudado e praticado, mas ainda sem ver a verdade que eles contêm,
então ainda não teremos um lar. <o:p></o:p></span></p>

<p class=Normal>
Portanto,
compreenda esse ponto de que todas as pessoas, todas as criaturas, estão
prestes a ir embora. Quando os seres viverem um certo tempo eles seguirão o seu
caminho. O rico, o pobre, o jovem, o velho, todos os seres irão experimentar
essa mudança. <o:p></o:p></span></p>

<p class=Normal>
Quando você se
der conta de que o mundo é assim, você sentirá que é um lugar que incomoda
muito. Quando você se der conta de que não há nada estável ou sólido em que você
possa confiar, você se sentirá incomodado e desencantado. Sentir-se
desencantado no entanto não significa que você terá aversão. A mente estará
clara. Ela vê que não existe nada que possa ser feito para remediar esse estado
de coisas, assim é o mundo. Sabendo disso, você pode soltar o apego, soltar com
a mente que não está feliz nem triste mas em paz com os sankharas, pois vê com
sabedoria a sua natureza de mudança constante. <o:p></o:p></span></p>

<p class=Normal><i >Anicca vata sankhara</span></i>  – todas as formações são impermanentes. Colocando de
maneira simples: impermanência é o Buda. Se vemos um fenômeno impermanente com
clareza, veremos que ele é permanente, permanente no sentido de que a sua
sujeição à mudança é constante. Essa é a permanência que os seres vivos
possuem. Existe transformação contínua, da infância através da juventude até a
velhice, e essa mesma impermanência, essa disposição à mudança é fixa e
permanente. Se você encará-lo dessa forma o seu coração estará em paz. Não é
somente você que tem que passar por isso, são todos.<o:p></o:p></span></p>

<p class=Normal>
Quando você
considera as coisas dessa forma, você as verá como um incômodo e o
desencantamento irá surgir. O seu deleite com o mundo dos prazeres sensuais irá
desaparecer. Você verá que se tiver muitas coisas, terá que abandonar muitas
coisas; se tiver poucas, abandonará poucas. A riqueza é somente riqueza, uma
vida longa é somente uma vida longa, elas não têm nada de especial. <o:p></o:p></span></p>

<p class=Normal>
O que é
importante é que façamos o que o Buda nos ensinou e que construamos o nosso
lar, construi-lo utilizando o método que eu estive lhe explicando. Construa o
seu lar. Solte tudo. Solte até que a mente alcance a paz que está livre de
avançar, livre de retroceder e livre de ficar imóvel. O prazer não é o nosso
lar, a dor não é o nosso lar. O prazer e a dor, ambos, decaem e desaparecem. <o:p></o:p></span></p>

<p class=Normal>
O Grande Mestre
viu que todos os sankharas são impermanentes e por isso ele nos ensinou a
soltar o nosso apego por eles. Quando chegarmos ao fim das nossas vidas, não
teremos escolha de qualquer maneira, não seremos capazes de levar nada conosco.
Portanto não seria melhor se soltar das coisas antes disso? Elas são somente um
fardo pesado que carregamos; porque não se desfazer desse fardo agora?<span
style="mso-spacerun: yes">  </span>Porque o esforço de carregá-lo conosco?
Solte tudo, relaxe e deixe que a sua família tome conta de você. <o:p></o:p></span></p>

<p class=Normal>
Aqueles que
cuidam de um enfermo, cultivam a bondade e a virtude. Alguém que esteja doente
e que oferece aos outros essa oportunidade não deveria dificultar ainda mais as
coisas para eles. Se existe dor ou algum outro problema, diga-lhes e mantenha a
mente em um estado saudável. Aquele que estiver cuidando dos pais deve
preencher a sua mente com cordialidade e bondade e não se deixar pegar pela
aversão. Essa é a ocasião em que você pode pagar o seu débito para com eles.
Desde o seu nascimento através da infância, conforme você crescia, você esteve
na dependência dos seus pais. Estamos aqui hoje porque as nossas mães e pais
nos ajudaram de tantas formas. Devemos a eles muita gratidão. <o:p></o:p></span></p>

<p class=Normal>
Portanto hoje,
todos vocês, crianças e parentes aqui reunidos, juntos, vejam como os seus pais
se tornam as suas crianças. Antes vocês eram as crianças deles; agora eles são
as crianças de vocês. Eles ficam cada vez mais velhos até que se convertem em
crianças novamente. A sua memória fica falha, os olhos já não vêm tão bem e os
seus ouvidos não escutam, às vezes misturam as palavras. Não deixem que isso os
perturbe. Todos vocês que cuidam dos doentes têm que aprender a soltar. Não se
agarrem às coisas, simplesmente soltem-nas e que sigam o seu próprio curso.
Quando uma criança é desobediente, às vezes os pais permitem que ela faça o que
quer como forma de manter a paz, para fazê-la feliz. Agora os seus pais são
como essa criança. A memória e a percepção deles está confusa. Certas vezes
eles confundem os seus nomes, ou vocês lhes pedem para trazer uma xícara e eles
trazem um prato. É normal, não fiquem transtornados por isso. <o:p></o:p></span></p>

<p class=Normal>
Que o paciente
possa se lembrar da bondade daqueles que cuidam dele e pacientemente suporte as
sensações de dor. Faça um esforço, não permitindo que a mente se torne dispersa
e agitada e não dificultando as coisas para quem está cuidando de você. Permita
que aqueles que cuidam dos doentes preencham a sua mente<span
style="mso-spacerun: yes">  </span>com virtude e bondade. Que não tenham
aversão pelo aspecto repugnante do trabalho, de limpar o catarro e a fleuma ou
a urina e excremento. Façam o melhor possível. Todos na família podem ajudar. <o:p></o:p></span></p>

<p class=Normal>
Eles são os
únicos pais que você possui. Eles lhe deram a vida, eles foram os seus mestres,
suas enfermeiras e seus médicos - eles foram tudo para você. Eles os criaram,
educaram, compartilharam as suas posses com você, fizeram de você o herdeiro,
esse é o grande benefício feito pelos seus pais. Como resultado, o Buda ensinou
as virtudes de <i style='mso-bidi-font-style:normal'>kataññu </i>e <i
style='mso-bidi-font-style:normal'>katavedi,</i> conhecer a nossa dívida de
gratidão e tentar repagá-la. Essas duas virtudes são complementares. Se os
nossos pais estão passando por necessidades, se não estão bem ou enfrentando
dificuldades, então fazemos o melhor possível para ajudá-los. Isto é<i
style='mso-bidi-font-style:normal'> kataññu-katavedi,</i> é uma virtude que
mantem o mundo. Previne que as famílias se separem, faz com que permaneçam
estáveis e harmoniosas. <o:p></o:p></span></p>

<p class=Normal>
Hoje eu lhes
trouxe o Dhamma como um presente nesta ocasião de enfermidade. Não tenho coisas
materiais para lhes dar; parece que já existe abundância dessas coisas na casa
e assim eu lhes dou o Dhamma, algo que possui um valor de longa duração, algo
que vocês nunca conseguirão esgotar. Tendo recebido de mim, vocês podem
passá-lo adiante para tantos outros que quiserem e ele nunca será esgotado.
Assim é a natureza da Verdade. Estou feliz por dar-lhes este presente do Dhamma
e espero que lhes dê força para lidar com a sua dor. <o:p></o:p></span></p>


