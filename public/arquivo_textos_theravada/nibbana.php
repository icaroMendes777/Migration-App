<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Todos sabemos o que ocorre
quando um fogo se extingue. As chamas morrem e o fogo desaparece. Portanto
quando pela primeira vez nos damos conta de que o objetivo da prática Budista,
nibbana (nirvana), significa literalmente a extinção de um fogo, é difícil
imaginar uma imagem mais mortífera para um objetivo espiritual: a completa
aniquilação. Acontece que, no entanto, essa interpretação desse conceito é um
erro de tradução, não tanto de palavra, mas de imagem. O que significava um
fogo extinto para os Hindus na época do Buda? Tudo, menos aniquilação. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>De acordo com os brâmanes
da antiguidade, quando um fogo se extinguia ele passava a um estado de
latência. Ao invés de deixar de existir, ele se tornava dormente e nesse estado
- desatado de qualquer combustível em particular - ele se espalhava através do
cosmos. Quando o Buda usou essa imagem para explicar nibbana para os brâmanes
Hindus da sua época, ele contornou a questão sobre se um fogo extinto continua
a existir ou não, focando ao invés disso na impossibilidade de definir um fogo
que não queima: por isso a sua afirmação de que uma pessoa que está totalmente
'extinta' não pode ser descrita. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>No entanto, ao ensinar para
os seus próprios discípulos, o Buda usava nibbana mais como uma imagem de
liberdade. Aparentemente, todos Hindus naquela época consideravam o fogo
queimando como agitado, dependente e aprisionado, ambos, apegado e preso ao seu
combustível enquanto queimava. Para acender um fogo era necessário<span
style="mso-spacerun: yes">  </span>&quot;dominá-lo&quot;. Quando o fogo se soltava
do seu combustível, ele estava &quot;livre&quot;, liberto da agitação,
dependência e aprisionamento - calmo e sem limitações. É por isso que as
poesias em Pali repetidamente usam a imagem do fogo extinto como uma metáfora
para a liberdade. Na verdade, essa metáfora faz parte de um modelo figurativo
do fogo que também envolve outros dois termos relacionados. <i>Upadana,</i> ou
apego, também se refere ao sustento que um fogo obtém do seu combustível. <i>Khandha</i>
significando não somente um dos cinco &quot;agregados&quot; (forma, sensação,
percepção, formações e consciência) que definem todas as experiências
condicionadas, mas também o tronco de uma árvore. Tal como o fogo se extingue
quando pára de se apegar e obter o sustento da madeira, da mesma forma a mente
está liberta quando pára de se apegar aos <a name=PVW>khandhas</a>. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Portanto, a imagem que está
por trás de nibbana é de liberdade. Os comentários em Pali suportam esse ponto
ao rastrear a palavra nibbana até a sua raiz verbal, que significa
&quot;desatamento&quot;. Que tipo de desatamento? Os textos descrevem dois
níveis. Um é o desatamento nesta vida, simbolizado por um fogo que se apagou
mas cujas brasas ainda estão quentes. Isso representa o arahant iluminado, que
tem consciência de visões e sons, tem sensibilidade ao prazer e à dor, mas está
livre da paixão, aversão e delusão. O segundo nível de desatamento, simbolizado
por um fogo que está de tal forma extinto que as suas brasas já esfriaram, é
aquilo que o arahant experimenta após esta vida. Todos os estímulos aos
sentidos se esfriam e ele/ela está totalmente liberto/a até das mais sutis
limitações e estresses da existência no tempo e espaço. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O Buda insiste em que esse
nível não pode ser descrito, mesmo em termos de existência ou não existência,
porque as palavras somente têm efeito para aquelas coisas que possuem limites.
Tudo que ele realmente diz a respeito disso – exceto pelas imagens e metáforas
– é que uma pessoa pode ter um conhecimento parcial dessa experiência nesta
vida e que isso é a felicidade última, algo que verdadeiramente vale a pena
conhecer.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim, da próxima vez que
você observar um fogo se apagando, não veja isso como um caso de aniquilação,
mas como uma lição de como a liberdade pode ser encontrada ao se soltar das
coisas.</span></p>


