<p class=Normal>Vinaya é o nome para o conjunto de regras e tradições monásticas que são obrigatórias para todos os monges e as monjas Budistas. O Vinaya foi estabelecido pelo próprio Buda e agora está preservado sob a forma escrita, tanto nas línguas da antiga Índia bem como traduzido para idiomas Ocidentais.</p>

<p class=Normal>    Com tantas pessoas novas tomando contato com o Budismo nos últimos anos muitas conhecem muito pouco sobre as regras de disciplina da comunidade monástica. É importante que a comunidade leiga tenha uma boa compreensão dessas regras de modo a assegurar que não se comportem de alguma forma que possa ser ofensiva à Sangha, nem que possa lhes criar dificuldades. </p>

<p class=Normal><b>    Propriedade e Administração de Monastérios:</b></p>

<p class=Normal>    Na época do Buda, quando um Budista leigo oferecia terras, edifícios, ou dinheiro para estabelecer um monastério, ele dedicava a doação para a Sangha dos quatro quadrantes no presente e ainda por vir. A Sangha dos quatro quadrantes no presente e ainda por vir significa todos os monges e monjas ordenados da forma correta. Isso inclui todos os monges e monjas Budistas legítimos, de todas as nacionalidades e seitas. Hoje isso provavelmente incluiria a maioria dos monges e monjas chineses Mahayana (bhiksus e bhiksunis), mas excluiria alguns lamas tibetanos e a maioria dos roshis zen, pelo menos os casados! Portanto, os proprietários dos monastérios são a comunidade mundial e temporal de monges e monjas.</p>

<p class=Normal>    Os administradores do monastério eram os monges e monjas que lá viviam. Eles se reuniam periodicamente para tomar decisões relativas ao seu monastério e todas essas decisões tinham que ser unânimes. Mas, a fim de proteger o monastério de monges corruptos, há muitas regras no Vinaya que restringem o que os monges residentes poderiam fazer. Por exemplo, eles não poderiam decidir doar propriedades da Sangha (a menos que fosse insignificante), nem dividir os bens entre eles mesmos, (depois abandonar os mantos, vender tudo e mudar para a praia!). A comunidade em um monastério tem a obrigação de preservar e manter em bom estado todos os bens da Sangha, preservando-os em benefício da comunidade atual e no futuro.</p>

<p class=Normal>Em grandes monastérios, e alguns tinham milhares de monges e monjas, a comunidade delegava algumas das suas responsabilidades para monges e monjas competentes. Assim, por exemplo havia um monge responsável pela alocação de alojamentos, e outro responsável pela construção e manutenção. O Venerável Maha Moggalana, um dos dois principais discípulos do Buda, foi talvez o monge mais eficaz na construção. Certa vez o Buda lhe encomendou, com o auxílio de 500 monges, a construção em Savathi, com recursos doados pela discípula Visakha, de um grandioso edifício chamada "Migaramatu Pasada". Essa habitação monástica tinha dois andares, cada um com 500 quartos com pináculos de ouro! Devido aos poderes supra-humanos do Venerável Maha Moggalana (na época eles não dispunham de guindastes e escavadeiras) foram necessários apenas nove meses para a conclusão do edifício. </p>

<p class=Normal>Concluindo, na época de Buda, a comunidade monástica residente administrava o seu monastério em todos os aspectos, mantendo-o em boas condições para o benefício de todos os monges e monjas, no presente e no futuro. E os monges participavam, embora só de vez em quando, de projetos de construção. Os famosos monastérios na antiga Índia, como o Jeta Grove em Savathi, onde o Buda passou 19 retiros das chuvas, eram propriedade da Sangha e operados pelos monges.</p>

