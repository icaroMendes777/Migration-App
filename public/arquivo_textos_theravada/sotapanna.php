<i>Ouvir o verdadeiro Dhamma é um fator para entrar na correnteza.</i><br>
<i>Atenção com sabedoria é um fator para entrar na correnteza.</i><br>
<i>Prática de acordo com o Dhamma é um fator para entrar na correnteza.</i><br>
