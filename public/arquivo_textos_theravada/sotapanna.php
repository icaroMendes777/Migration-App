<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Com relação a fatores externos, eu não vejo nenhum outro único fator como a amizade com pessoas admiráveis que faça tanto por um bhikkhu em treinamento, que ainda não atingiu o objetivo, mas que permanece decidido a alcançar a insuperável libertação do apego. Um bhikkhu que tem amizade com pessoas admiráveis abandona o que não é hábil e desenvolve o que é hábil.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Um bhikkhu que tenha pessoas admiráveis<br>
como amigos<br>
- que os honra, os respeita,<br>
que faz aquilo que os amigos lhe aconselham - <br>
com atenção plena e plena consciência,<br>
realiza passo a passo<br>
&nbsp;&nbsp;&nbsp; a eliminação de todos os grilhões.<o:p></o:p></span></p>

