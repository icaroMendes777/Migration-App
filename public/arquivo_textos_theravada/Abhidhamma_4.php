<p class=Tit1 align=center style='text-align:center'>IV. Compêndio do Processo Cognitivo<br> <i>(Vithisangahavibhaga)</i></Tit1>


<p class=Normal>&nbsp;</p>

<hr size=2 width="100%" align=center>

<p class=Normal>&nbsp;</p>





<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Este
capítulo trata da dinâmica da consciência tal como ela ocorre no processo
cognitivo. Todas as <i>cittas</i> em qualquer processo cognitivo ocorrem numa
dada seqüência de acordo com as leis da natureza. A ocorrência da consciência
fora do processo cognitivo, isto é, nas ocasiões do renascimento, <i>bhavanga</i>
e morte estão fora do escopo deste trabalho.<o:p></o:p></span></p>

<p class=Normal><b><name=T41></a>Enumeração das Categorias</b></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>No
compêndio do processo cognitivo devem ser compreendidas seis classes compostas
de seis elementos cada uma:<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>(i) seis
bases<br>

(ii) seis
portas<br>

(iii) seis
objetos<br>

(iv) seis
tipos de consciência<br>

(v)
seis
processos<br>

(vi)
apresentação
dos objetos de seis formas<o:p></o:p></span></p>


<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Seis
bases e Seis portas: </span></b><span lang=PT-BR style='mso-ansi-language:PT-BR'>naqueles
planos de existência onde ocorre a materialidade, as <i>cittas</i> e os <i>cetasikas</i>
surgem na dependência de uma condição chamada base, <i>(vatthu).</i> A base é
um suporte físico para a ocorrência da consciência. Embora as primeiras cinco
bases coincidam com as cinco primeiras portas, uma base não é idêntica a uma
porta, já que desempenha um papel diferente no processo da origem da
consciência. Uma porta é um canal através do qual as <i>cittas</i> e os <i>cetasikas</i>
de um processo cognitivo obtêm acesso ao objeto; uma base é um suporte físico
para a ocorrência de <i>cittas</i> e <i>cetasikas</i>. A base do coração serve
como o suporte físico para todos os tipos de consciência exceto aqueles
originados nos cinco meios dos sentidos. <i>Bhavanga </i>(<a href="#N1">1</a>)<i>
</i><a name=R1></a>é a porta da mente.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Seis
objetos: </span></b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Cada
consciência, juntamente com os seus fatores mentais associados, necessariamente
toma um objeto, pois a consciência em si consiste em essência na atividade da
cognição de um objeto. Em Pali duas palavras são usadas para caracterizar um
objeto. Uma é <i>arammana </i>que deriva de uma raiz com o significado de
“deliciar-se com.” A outra é <i>alambana </i>derivada de uma raiz totalmente
distinta que significa “agarrar-se a.” Portanto, o objeto é aquilo com o qual a
consciência e os seus concomitantes se deliciam ou no qual se agarram. No
Abhidhamma são reconhecidos seis tipos de objetos que correspondem aos seis
sentidos.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Seis
tipos de consciência</span></b><span lang=PT-BR style='mso-ansi-language:PT-BR'>:
os seis tipos de consciência são: consciência no olho, consciência no ouvido,
consciência no nariz, consciência na língua, consciência no corpo, consciência
na mente.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Seis
processos</span></b><span lang=PT-BR style='mso-ansi-language:PT-BR'>: <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>De acordo
com as portas os processos cognitivos são:<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> (i) o processo conectado com a porta do olho<br>

(ii) o processo conectado com a porta do ouvido<br>

(iii) o processo conectado com a porta do nariz<br>

(iv) o processo conectado com a porta da língua<br>

(v) o processo conectado com a porta do corpo<br>

(vi) o processo conectado com a porta da mente<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ou, de
acordo com a consciência os processos cognitivos são:<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> (i) o processo conectado com a consciência no olho<br>

 (ii) o processo conectado com a consciência no ouvido<br>

 (iii) o processo conectado com a consciência no nariz<br>

 (iv) o processoconectado com a consciência na língua<br>

 (v) o processo conectado com a consciência no corpo<br>
 (vi) o processo conectado com a consciência na mente<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O processo
cognitivo conectado com as portas deve ser coordenado com a consciência
correspondente.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os seis
processos cognitivos</span></b><span lang=PT-BR style='mso-ansi-language:PT-BR'>:
A palavra <i>vithi </i>tem o sentido literal de rua, mas aqui é empregada com o
sentido de processo. Quando as <i>cittas </i>surgem no processo de cognição de
um objeto nas portas dos sentidos ou na porta da mente, elas não ocorrem de
forma aleatória ou isolada, mas como fases numa série de eventos cognitivos
discretos, cada um conduzindo ao seguinte numa ordem regular e uniforme. Essa
ordem é chamada <i>cittaniyama, </i>a ordem do processo da consciência. Para
que um processo cognitivo ocorra, todas as condições essenciais têm que estar
presentes. Para cada tipo de processo as condições essenciais são as seguintes:<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> 
Para
um processo na porta do olho:<o:p></o:p></span></p>

<p class=Normal style='margin-left:1.0in'> (a) sensibilidade no olho <i>(cakkhuppasada)</i><br>

(b) objeto visível (<i>ruparammana</i>)<br>

(c) luz (<i>aloka</i>)<br>

(d) atenção (<i>manasikara</i>)<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>Para
um processo na porta do ouvido:<o:p></o:p></span></p>

<p class=Normal style='margin-left:1.0in'>(a) sensibilidade no ouvido <i>(sotappasada)</i><br>

(b) som <i>(saddarammana)</i><br>

(c) espaço <i>(akasa)</i><br>

(d) atenção<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>Para
um processo na porta do nariz:<o:p></o:p></span></p>

<p class=Normal style='margin-left:1.0in'> (a) sensibilidade no nariz (<i>ghanappasada</i>)<br>

(b) aroma <i>(gandharammana)</i><br>

(c) elemento ar (<i>vayodhatu)</i><br>

(d) atenção<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>Para um
processo na porta da língua<o:p></o:p></span></p>

<p class=Normal style='margin-left:1.0in'> (a) sensibilidade na língua (<i>jivhappasada)</i><br>

(b)sabor <i>(rasarammana)</i><br>

(c) elemento água <i>(apodhatu)</i><br>

(d) atenção<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>Para
um processo na porta do corpo<o:p></o:p></span></p>

<p class=Normal style='margin-left:1.0in'>(a) sensibilidade no corpo <i>(kayappasada)</i><br>

(b) objeto tangível <i>(photthabbarammana)</i><br>

(c) elemento terra (<i>pathavidhatu)</i><br>

(d) atenção<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>Para
um processo na porta da mente<o:p></o:p></span></p>

<p class=Normal style='margin-left:1.0in'>(a) a base do coração <i>(hadayavatthu)</i><br>

(b) objeto mental <i>(dhammarammana)</i>
<br>

(c) bhavanga
<o:p></o:p></span></i></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os seis
tipos de processo cognitivo são divididos em dois grupos: os processos que
ocorrem em cada uma das cinco portas físicas;<span style="mso-spacerun: yes"> 
</span>e os processos que ocorrem exclusivamente na porta da mente. Como os
processos nas cinco portas físicas também emergem de <i>bhavanga, </i>estes são
chamados de processos com portas mistas já que envolvem ambas, a porta da mente
e a porta de um órgão físico dos sentidos.<i> </i>Os processos que ocorrem
apenas na porta da mente são então chamados de processos na porta da mente já
que eles emergem de <i>bhavanga </i>sozinhos, sem a participação de uma porta
dos sentidos. Como será visto mais adiante, os primeiros cinco processos seguem
um padrão uniforme apesar das diferenças nas faculdades sensoriais através das
quais eles ocorrem, enquanto que o sexto compreende uma variedade de processos
cuja única semelhança é que eles ocorrem independentes das portas dos sentidos
externas.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Apresentação
dos objetos de seis formas: </span></b><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Isto significa a forma como um objeto se apresenta para a consciência
numa das seis portas, ou a ocorrência de estados de consciência a partir da
apresentação de um objeto. A apresentação de um objeto nas cinco portas dos
sentidos é analisada de quatro formas: muito intenso, intenso, leve e muito
leve. Na porta da mente existem duas alternativas: claro e obscuro. As palavras
intenso e leve não são usadas com referência ao objeto em si, mas em relação à
força do seu impacto na consciência. Que tão distinta é a impressão que o
objeto provoca. Por exemplo, se um objeto visível, com grande dimensão, se
apresenta na porta do olho, mas se a matéria sensível no olho for débil ou se a
luz for fraca, o objeto não irá criar uma impressão distinta e dessa forma irá
cair na categoria leve ou muito leve. Ou se por outro lado, uma forma pequena
ou sutil impacta o olho e devido à força da matéria sensível e à intensidade
luminosa o objeto criar uma impressão distinta, irá cair na categoria intenso
ou muito intenso. Dessa maneira, os termos qualificativos indicam não a
característica do objeto em si, mas o número de <i>cittas</i> que ocorrem no
processo cognitivo que surge, do momento em que o objeto impacta a porta do
sentido, até o momento em que a apresentação do objeto para a consciência
cessa. O mesmo princípio se aplica à apresentação dos objetos na porta da mente
como claro e obscuro.<o:p></o:p></span></p>

<p class=Normal><a name=T42></a><b>O Processo nas Cinco Portas dos Sentidos</b><o:p></o:p></span></[p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O tempo de
duração de uma <i>citta</i>, no Abhidhamma, é denominado um momento mental, (<i>cittakkhana</i>).
Essa é uma unidade temporal com duração extremamente curta, os comentários
dizem que bilhões de momentos mentais ocorrem num piscar de olhos. No entanto,
apesar da sua curta duração, cada momento mental consiste de três submomentos –
surgimento, <i>(uppada); </i>presença, <i>(thiti); </i>e dissolução, <i>(bhanga).
</i>Dentro do espaço de um momento mental, surge uma <i>citta</i>, esta realiza
a sua função e depois se dissolve, condicionando a <i>citta </i>que segue de
imediato. Desse modo, através da seqüência de momentos mentais, o fluxo da
consciência segue sem interrupção como as águas de um rio.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os
fenômenos materiais também passam por esses três estágios de surgimento,
presença e dissolução, mas neste caso, o tempo requerido para que esses três
estágios sejam completados é igual ao tempo necessário para que dezessete <i>cittas
</i>surjam e desapareçam. <o:p></o:p></span></p>


