<p class=Normal>Jhana
significa estar absorto ou focado num único objeto, como quando nos ocupamos
com a respiração. <o:p></o:p></span></p>


<p class=Normal>O
PRIMEIRO<span style="mso-spacerun: yes"> </span>JHANA tem cinco fatores: (a)
Pensamento aplicado: Pense na respiração até que você possa reconhecê-la com
clareza sem se distrair. (b) Unicidade da mente: Mantenha a mente com a
respiração. Não permita que ela se distraia com outros objetos. Vigie seus
pensamentos de modo que eles se ocupem apenas com a respiração até que a
respiração fique confortável. (A mente se torna una, em paz com a respiração)
(c) Pensamento sustentado: Permita que essa sensação confortável da respiração
se espalhe e se coordene com as outras sensações no corpo. Deixe que essas
sensações da respiração se espalhem até que todas fluam juntas. Uma vez que o
corpo tenha sido tranqüilizado pela respiração, sensações de dor irão se
acalmar. O corpo estará<span style="mso-spacerun: yes"> </span>repleto de
energia positiva da respiração. Essas três qualidades têm de estar aplicadas no
mesmo fluxo da respiração para que o primeiro jhana surja. Esse fluxo da
respiração poderá então conduzí-lo por todo o caminho até o quarto jhana. O
pensamento aplicado, a unicidade da mente e o pensamento sustentado agem como
as causas. Quando as causas estão maduras, os resultados aparecem. (d) Êxtase:
uma sensação convincente de satisfação e refrescamento para o corpo e a mente,
que vai direto ao coração, independente de todo o resto. (e) Felicidade:
conforto corporal proveniente da tranqüilidade e imperturbabilidade do corpo; satisfação
mental proveniente do fato da mente estar tranqüila por si mesma, sem
perturbações, serena e exultante. O êxtase e a felicidade são os resultados. Os
fatores do primeiro jhana então se resumem em dois tipos: causas e resultados. <o:p></o:p></span></p>


<p class=Normal>À medida
que o êxtase e a felicidade se fortalecem, a respiração fica mais sutil. Quanto
mais tempo você permanecer focado, mais poderosos se tornam os resultados. Isso
permite que você deixe de lado o pensamento aplicado e o pensamento sustentado
e dependendo exclusivamente de um único fator, unicidade da mente, você entra
no segundo jhana. <o:p></o:p></span></p>



<p class=Normal>O SEGUNDO
JHANA tem três fatores: êxtase, felicidade e unicidade da mente. O êxtase e a
felicidade se fortalecem no segundo jhana porque eles dependem de uma única
causa,<span style="mso-spacerun: yes">  </span>unicidade da mente, que assume a
tarefa a partir deste ponto: focando na respiração para<span
style="mso-spacerun: yes">  </span>que esta se torne cada vez mais refinada,
mantendo-a estável e tranqüila com uma sensação refrescante e confortável para
ambos, corpo e mente. A mente estará ainda mais estável e resoluta do que
antes. Enquanto você continua focando, o êxtase e a felicidade se incrementam e
começam a pulsar. Continue focando na respiração, movendo a mente para um nível
sutil mais profundo para escapar dos movimentos do êxtase e da felicidade, e
você entrará no terceiro jhana. <o:p></o:p></span></p>


<p class=Normal>O TERCEIRO
JHANA tem dois fatores: felicidade e unicidade da mente. O corpo estará quieto,
imóvel e solitário. Nenhuma sensação de dor surge para perturbá-lo. A mente
estará solitária e tranqüila. A respiração refinada, fluindo livremente e
ampla. Uma luminosidade – branca, como algodão – permeia todo o corpo,
acalmando todas as sensações de desconforto físico e mental. A respiração
preenche o corpo. A atenção plena preenche o corpo.<o:p></o:p></span></p>



<p class=Normal>Foque em: A
mente está luminosa e poderosa, o corpo está leve. A sensação de felicidade se
acalmou. A sua noção do corpo está estável e equilibrada, sem lapsos ou
intervalos na sua atenção, assim você pode abandonar a sua sensação de
felicidade. A unicidade da mente, a causa, tem força suficiente para focar com
mais intensidade, conduzindo-o ao quarto jhana. <o:p></o:p></span></p>


<p class=Normal>O QUARTO
JHANA tem dois fatores: equanimidade e unicidade da mente, ou atenção plena –
sólida, estável e segura. A propriedade da respiração está absolutamente quieta
e tranqüila, livre de ondulações e intervalos, tal qual um oceano livre de
correntes ou ondas. A mente, neutra e tranqüila, abandona todas as preocupações
com o passado e o futuro. O presente é neutro e tranqüilo. Essa é a verdadeira
unicidade da mente, focada na imperturbável tranqüilidade da respiração. Todos
os aspectos da energia da respiração no corpo se conectam de modo que você pode
respirar por todos os poros. Você não precisa respirar pelas narinas porque a
inspiração e a expiração e os outros aspectos da respiração no corpo formam um
todo único, unificado – equilibrado e pleno. A atenção plena e a plena
consciência convergem dando origem a uma grande energia capaz de dissipar toda
a obscuridade interior. <o:p></o:p></span></p>


