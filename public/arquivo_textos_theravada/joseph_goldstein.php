<p class=Tit1 align=center style='text-align:center'>Joseph Goldstein</Tit1>

<p class=Normal>Joseph Goldstein tem dirigido retiros de meditação vipassana e amor bondade em todo o mundo desde 1974. Ele é um dos fundadores da Insight Meditation Society em Barre, Massachusetts, onde é um dos professores residentes. Em 1989, juntamente com vários outros professores e estudantes de meditação vipassana ajudou a estabelecer o Barre Center for Buddhist Studies. Joseph teve seu  interesse despertado pelo Budismo quando era um voluntário no Peace Corps na Tailândia.<o:p></o:p></span></p>

<p class=Normal>Ele tem estudado e praticado meditação vipassana desde 1967, sob a orientação de vários mestres como Anagarika Munindra, S.N. Goenka, Dipa Ma ( Nani Bala Barua) e mais recentemente o Venerável U Pandita Sayadaw de Myanmar. Ele é o autor de: <i>Insight Meditation: The Practice of Freedom  e  The Experience of Insight: A Simple and Direct Guide to Buddhist Meditation  e co-autor do  Seeking the Heart of Wisdom: The Path of Insight Meditation.</i><o:p></o:p></span></p>


