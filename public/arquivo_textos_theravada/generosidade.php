<p class=Normal>A questão principal para muitas pessoas é como praticar a meditação na vida
diária. Como praticar o Dhamma na vida diária. A prática de meditação formal em
um retiro é antes de mais nada um treinamento intensivo em um ambiente bastante
estruturado. Isso ajuda e é importante, mas a verdadeira prática de meditação,
se quisermos que a meditação traga um benefício real, ocorre na nossa vida
diária.</p>

<p class=Normal>Na vida diária, o Caminho completo e os demais aspectos do cultivo da mente
também devem ser encarados e praticados. Realmente é na nossa vida diária, nas
situações do dia a dia que <i style='mso-bidi-font-style:normal'>precisamos </i>ter
habilidade e compreensão para enfrentar todos os desafios que surgem: todas as
situações de conflito, o caos, os altos e baixos de cada dia. </p>

<p class=Normal>Precisamos ter uma estratégia para reconhecer e enfrentar as impurezas que
surgem nas nossas mentes bem como os negativismos e impurezas de outras pessoas
que nos atingem. Necessitamos desenvolver qualidades da mente adicionalmente à
meditação. <span lang=PT-BR style='font-size:11.0pt;mso-bidi-font-size:12.0pt;
mso-ansi-language:PT-BR'><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Muitas pessoas querem
meditar e encontrar a paz de espírito. Mas algumas delas não querem na verdade
mudar o resto do seu estilo de vida. Elas querem comer o bolo e guardá-lo ao
mesmo tempo - serem capazes de meditar e obter os benefícios, como a paz de
espírito, mas ainda serem capazes de fazer tudo aquilo que lhes venha à mente
de acordo com os seus caprichos e a sua imaginação. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Mas, o processo realmente
não funciona assim. No geral, a mente que encontramos quando sentamos em
meditação - todos os estados que surgem, as emoções difíceis, os outros estados
negativos da mente e mesmo a nossa condição física, com dores e outras coisas
do gênero - é basicamente o resumo de tudo aquilo que estivemos acumulando
durante a nossa vida. Essas acumulações são as conseqüências dos nossos hábitos
ou estilos de vida e até mesmo das nossas opiniões.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Existem práticas, alem da
meditação, que podemos cultivar para nos ajudar a incorporar o Dhamma aos
nossos hábitos, estilo de vida e às nossas opiniões. Vamos explorar alguns
desses outros aspectos da prática do Dhamma que devemos colocar em ação nas
nossas vidas diárias, de acordo com as situações que surgirem.<i
style='mso-bidi-font-style:normal'><o:p></o:p></i></span></p>

<p class=Normal>Sabemos que a segunda Nobre Verdade define a origem do sofrimento como cobiça e  apego, o desejo desenfreado. Por causa disso, uma das principais práticas do Dhamma é chamada de <i>Dana</i>. <i>Dana</i> significa a prática da generosidade ou o ato de compartir com os outros. É um antídoto contra o apego, a esse agarrar-se com desespero às coisas, ao apego desmedido às nossas coisas. Encontramos essa cobiça e apego em todos os lugares. Agarramo-nos às nossas posses e não queremos nos soltar delas. O problema é que, quanto mais possuirmos, maior será o fardo. A prática da generosidade pode ser de grande ajuda, pois ela é um antídoto contra a mesquinharia. Além disso, o ato de compartilhar as nosas posses com os outros, ou o abandono do nosso egoísmo, ajuda a abrir as nossas mentes para o amor bondade e a compaixão. E estes são um antídoto contra o apego e o desejo. </p> 
<p class=Normal>A generosidade possui formas distintas. Pode-se dizer que existem três níveis de generosidade. O primeiro se chama <i>dar com u’a mão</i>. Com esse tipo de generosidade você dá porque as pessoas pedem ou porque você é pressionado a fazê-lo, ou ainda porque as pessoas estão olhando. Mas você continua segurando com u’a mão. Você, na verdade não quer dar e assim o faz com relutância. Digamos que um mendigo o importune. Para livrar-se dele, você lhe dá algo. Se você já esteve na Índia, provavelmente enfrentou situações em que os mendigos o seguem como uma sombra e não o abandonam até que finalmente você lhes dê algo. Essa é uma forma de generosidade, de compartir com os outros. Mas ela possui um valor limitado, porque o verdadeiro espírito imbuído no ato de dar é de realmente se soltar do que é doado, abandonar as coisas. Esse tipo de generosidade é abandono até certo ponto, e portanto, incompleto. </p> 
<p class=Normal>O segundo nível de generosidade é <i>dar amistosamente</i>. Quer dizer, você dá porque gosta de dar. Você se sente bem. Não é necessário pressão para que você o faça. Sempre que você vê alguém numa situação de necessidade, se você tem o suficiente para si mesmo, e possui o objeto da doação em dobro, você o dá com um sentimento de amizade. Se você tem duas bananas e alguém está com fome, você habitualmente dará uma. Essa é uma forma mais elevada de generosidade porque você não é pressionado a fazê-la - ela vem do seu próprio sentimento de amizade e você não está desesperado e firmemente agarrado às coisas. </p>
<p class=Normal>O terceiro nível de generosidade é <i>dar como um rei</i>. Ao dar como um rei, você dá qualquer coisa em qualquer momento. Você dá a sua camisa. Você dá o seu último alimento para alguém que esteja com mais fome. Como não existe o pensamento precedendo a ação, você dá o <i>melhor</i> que tiver. Não existe nenhum apego, nem mesmo a idéia de um "eu" envolvido no ato generoso. </p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Dar coisas materiais pode
ser a forma mais fácil de generosidade, especialmente quando você tem em
abundância. A maioria das pessoas, especialmente no Ocidente, possuem em
abundância. Temos armários e garagens cheias de coisas; temos roupas que não
usamos. Talvez façamos uma limpeza geral uma vez ao ano e doamos coisas para
entidades de caridade por generosidade. É claro que muitas vezes, estamos
limpando os armários de coisas que não necessitamos porque precisamos de mais
espaço para mais coisas que queremos acumular. Dar coisas materiais, dar
alimento, dar dinheiro para instituições de caridade, essas são todas formas de
generosidade material. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Uma outra forma de
generosidade é dar o seu tempo. Isso vai um pouco mais fundo, porque o seu
tempo está mais próximo do seu ego. É relativamente fácil dar uma moeda ou
alguma comida a um mendigo quando você possui em abundância, mas compartir o
seu tempo pode ser um pouco mais complicado. Imagine que o seu vizinho venha e
lhe diga, &quot;Ah, você sabe, estou com a situação muito complicada, eu
realmente necessito a sua ajuda neste Sábado para me ajudar a pintar a minha
casa.&quot;<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Sábado! Oh, meu Deus.
Esse é o dia do jogo de futebol. Não podemos pintá-la no Domingo?&quot; Ou,
&quot;Eu irei pedir ao meu sobrinho. Eu lhe darei algum dinheiro e pedirei que
ele o ajude.&quot;<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Nos apegamos ao nosso tempo
precioso e ao desejo de fazer somente aquilo que queiramos. Abandonar os nossos
próprios desejos e o nosso tempo, para ajudar uma pessoa que necessita é uma
forma mais profunda de generosidade. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Compartir o nosso
conhecimento ou talento com outros é uma outra forma de generosidade. Todas
essas formas de generosidade – desde a material até a mental - são formas de se
soltar/abandonar as coisas. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A meditação também é um
tipo de generosidade, de soltura/abandono. Na verdade pode se dizer que quando
meditamos essa é a forma mais elevada de abrir mão, porque estamos abandonando
tudo que surge através dos nossos sentidos, particularmente na meditação da
atenção plena. Estamos nos soltando do som que entra pelo ouvido, quer seja um
som prazeroso ou doloroso, deixamos apenas que ele surja e desapareça sem nos
apegarmos a ele. Se por acaso nos apegamos, tentamos nos soltar dele. Tentamos
nos soltar dos nossos pensamentos, nos soltar das dores no corpo. E é claro que
em última análise, cada um de nós tenta se soltar do eu. Nós nos soltamos da
sensação do eu de modo a compreender o Dhamma incondicionado e a verdadeira
libertação da mente. Para isso, até mesmo a noção do eu deve ser abandonada.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Com certeza, se não podemos
nos soltar de coisas materiais, coisas mentais, de emoções como a raiva, de
outros estados negativos ou mesmo estados positivos, então quando chegar o
momento, não seremos capazes de nos soltarmos do eu na meditação, de realizar o
salto significativo para a experiência incondicionada. Dessa forma, a prática
da generosidade é uma prática integral e completa em si mesma.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Na vida diária vocês podem
encontrar muitas oportunidades para praticar a generosidade. Você pode ser
especialmente generoso com o seu tempo quando alguém precise de ajuda, por
exemplo, alguém no trabalho pode dizer: &quot;Você poderia me mostrar como
funciona este computador?&quot; Mostre como fazê-lo, ou ajude-o ou seja
generoso de outras formas.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Existem três fundamentos do
Dhamma que nos auxiliam ao praticar a generosidade. Eles são o Entendimento
Correto, o primeiro aspecto do Nobre Caminho Óctuplo; Atenção Plena Correta, o
sétimo aspecto; Esforço Correto, o sexto aspecto. Todos estes três atuam em
conjunto.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O Entendimento Correto
compreende o egoísmo e a mesquinharia como sendo estados negativos. A Atenção
Plena Correta nos assegura que quando o egoísmo retornar ou intervir, nós o
veremos; nós notaremos quando as nossas mentes estiverem desesperadamente
agarrando as coisas. Tomando consciência de que o egoísmo e o apego são estados
prejudiciais da mente, usaremos o Esforço Correto para abandoná-los quando eles
surgirem. Praticando o Esforço Correto faremos o esforço para prevenir e
abandonar os estados prejudiciais, faremos o esforço para cultivar e
aperfeiçoar os estados benéficos. </span><span lang=PT-BR style='font-size:
11.0pt;mso-bidi-font-size:12.0pt;mso-ansi-language:PT-BR'><o:p></o:p></span></p>


