<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>Inquiring
Mind:</b> O que o levou a escrever esse novo livro, ‘Um Dharma’?<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>Joseph
Goldstein: </b>A motivação veio da minha experiência ao estudar com mestres de
várias tradições Budistas, em diferentes escolas dentro do Theravada, bem como
do Zen e da tradição Tibetana. Eu me dei conta que os ensinamentos e mestres
estavam dizendo coisas bastante diferentes, e com freqüência contraditórias,
sobre a natureza da mente ou a natureza da consciência e da libertação. Assim
eu me vi confrontado com a questão sobre o que fazer quando meus mais
respeitados mestres estavam dizendo coisas diferentes sobre aquilo que é da
maior importância para mim. Minha primeira reação foi a pergunta, Quem está
certo? Mas isso levantou a questão que se um está certo, então o outro tem que
estar errado. Eu me atormentei por causa disso por um bom tempo. No final das
contas, compreendi que através do raciocínio nunca poderia solucionar essas
questões da verdade última, então, desisti de tentar compreendê-las no nível
intelectual.<span style="mso-spacerun: yes">  </span>Um mantra surgiu na minha
mente, que agora uso tanto na minha prática como ao ensinar o Dharma: Quem
sabe? Qual é a natureza do despertar do Buda? Quem sabe? E até que sejamos um
Buda, nós não saberemos. Uma vez que aceitei isso, ao invés de me sentir
confuso, senti como se tivesse me soltado de um monte de apegos. Eu me senti
realmente aberto para ouvir todos os diferentes tipos de ensinamentos. E então
comecei a compreender – e esse é um ponto crucial daquilo que tento expressar
no livro – que todos os ensinamentos são vistos de uma forma mais benéfica
quando encarados como meios hábeis ao invés de afirmações sobre a verdades
última. Se tomarmos os ensinamentos como afirmações sobre a verdade, então
idéias opostas irão levar inevitavelmente ao conflito. Podemos ver com
facilidade como as idéias sectárias formam a base de tanto conflito no mundo de
hoje. Mas, se virmos os ensinamentos como meios hábeis para libertar a mente,
então poderemos adotar aquilo que tem valor de uma ampla gama de ensinamentos,
mesmo quando eles digam coisas bastante diferentes. Nosso critério precisa
apenas checar se os<span style="mso-spacerun: yes">  </span>ensinamentos irão
nos ajudar a libertar a mente. Isso levanta uma outra questão: O que significa
libertar a mente? Da minha prática com as diferentes tradições, eu descobri que
tudo se reduz a desenvolver uma mente que não se apegue a nada. Eu me lembro de
um conhecido ensinamento do Buda: “Nada, seja o que for, deve ser agarrado como
eu ou meu. Quem ouvir isto terá ouvido todo o ensinamento, quem praticar isso
terá praticado todo o ensinamento, quem compreender isso terá compreendido todo
o ensinamento.”<span style="mso-spacerun: yes">  </span>O que emerge no final é
uma tremenda simplicidade. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><B>IM:</B> Uma
idéia especial ou ensinamento não poderia ter um efeito poderoso sobre a
abordagem de uma pessoa para libertar a mente? Por exemplo, uma escola do Zen
afirma que nós já estamos iluminados e que quando sentamos em meditação estamos
expressando nossa iluminação, enquanto que outra escola diz que ainda não
estamos iluminados e meditamos para alcançar este estado.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><B>JG:</B> Eu
encontrei duas abordagens básicas para a prática. Uma chamo de “varrer por
cima,” no sentido de que já estamos iluminados e sentamos para expressar ou
compreender isso. A outra abordagem chamo de “construir por baixo,” onde
começamos com o reconhecimento do nosso sofrimento, com um esforço para aliviar
esse sofrimento. Existem vantagens e desvantagens em cada abordagem. Se alguém
for pego numa situação de muito sofrimento, provavelmente não irá ajudar muito
sugerir que ele já possui a pureza essencial. Ele precisa desenvolver uma
atenção plena muito clara daquilo que está surgindo no momento e trabalhar a
partir dessas dificuldades. Por outro lado, o perigo da abordagem de “construir
por baixo” é que as pessoas podem passar a ter fixação pelo seu sofrimento e ao
lutar contra ele poderão solidificar ainda mais a noção de um eu. Para essas
pessoas, no momento correto, ouvir um ensinamento sobre a pureza essencial da
mente pode ser a medida exata para fazer com que elas se abram para a
transparência do eu. O ensinamento correto, ou meio hábil, realmente depende
daquilo que cada indivíduo necessita. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><B>IM:</B> O
subtítulo do seu livro é &quot;The Emerging Western Buddhism,&quot; (O
Emergente Budismo Ocidental) e você fala sobre o quão afortunados somos em ter
todas essas diferentes correntes do Dharma do Buda disponíveis. Poderia haver
algum perigo por termos opções em demasia, meios hábeis em excesso?<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><B>JG:</B> A
abundância de métodos nos permite encontrar um método adequado ao nosso
temperamento e propensões. Mas é realmente importante, como praticantes, seguir
um método de prática ao qual nos adaptemos bem e seguí-lo até alcançar um
entendimento mais profundo. Em algum ponto pode ser possível e benéfico estudar
em diferentes tradições, mas se fizermos isso muito cedo, apenas criaremos
muita confusão. As pessoas algumas vezes podem se tornar diletantes, praticando
um pouco disto e um pouco daquilo sem na verdade obter qualquer profundidade.
No entanto, uma vez que tenhamos obtido uma base sólida numa prática, então
poderemos integrar os outros ensinamentos nessa base.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><B>IM:</B> Como
uma pessoa poderia saber o momento correto de tentar outros métodos ou
ensinamentos?<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><B>JG:</B> Podemos
saber, antes de mais nada, por meio da nossa própria experiência. Se estivermos
confusos, ou com muitas dúvidas sobre nossa prática, pode não ser o melhor
momento para mesclar outros ensinamentos. Uma outra forma de sabermos é através
da orientação de um mestre. Muitos estudantes têm me perguntado se devem fazer
alguma outra prática e meu conselho sempre depende do seu nível de prática e se
é apropriado para eles naquele momento ou não.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><B>IM:</B> Em que
estágio nos encontramos no desenvolvimento de um verdadeiro Budismo Ocidental?<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><B>JG:</B> Ao ser
perguntado sobre qual seria a aparência do Budismo Ocidental, um mestre
Asiático disse, “Saberemos em algumas centenas de anos.” Embora ainda estejamos
no estágio inicial, já é possível ver as pessoas praticando em diferentes
tradições. As pessoas com freqüência listam como seus mestres Sayadaws
Birmaneses, Mestres Tibetanos ou Zen, Ajaans Tailandeses e Mestres Ocidentais
de várias tradições. E se essa diversidade estiver fundamentada na estabilidade
e profundidade de uma prática, poderá ser de grande ajuda. Também quero dizer
que, para algumas pessoas, permanecer com apenas um método todo o tempo está
perfeito. Uma abordagem não é melhor ou pior que outra. É tudo uma questão de
meios hábeis. Se um método desenvolve o desapego, ele é um caminho para a mente
liberta. Eu acredito que o Budismo Ocidental Emergente terá precisamente essa
qualidade de pragmatismo que é tão característica da nossa cultura.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><B>IM:</B> Você
diria que a sua própria prática é um exemplo do Budismo Ocidental Emergente, de
“um dharma”? <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><B>JG:</B> Quando
as pessoas me perguntam sobre o que pratico – se é vipassana ou dzogchen? – eu
digo que é “desapego.” A prática se tornou simples assim: desapego e em seguida
os momentos de reconhecimento do apego, num próximo momento o desapego e assim
por diante. O método próprio de realizar essa prática pode mudar
constantemente: algumas vezes sentimos que é apropriado estabilizar a mente na
respiração; em outras a intuição é de permanecermos com a atenção mais aberta,
no vazio; outras vezes a intuição é de liberar a energia do corpo através da
técnica de varredura. A prática é guiada pela intuição e ela é coerente,
perfeitamente coerente. Ao tentar destilar os aspectos essenciais do Dharma do
Buda da minha própria prática, encontrei, de uma forma ou de outra, a atenção
plena como método, a compaixão como manifestação do dharma e a sabedoria como
essência. Essas três qualidades – atenção plena, compaixão e sabedoria –
parecem ser os elementos necessários. Esses três elementos compõem o oceano do
dharma, para o qual fluem como rios todas as tradições. As diferentes tradições
podem enfatizar um ou outro desses elementos, mas juntas elas tecem o dharma
único da libertação. <o:p></o:p></span></p>


