<p class=Tit1 align=center style='text-align:center'>Bhante Henepola Gunaratana (Nayaka Thera)</Tit1>

<p class=Normal>Henepola Gunaratana é um monge Budista nascido em 1927 no Sri Lanka. Ele foi ordenado como noviço aos 12 anos de idade em um pequeno templo no vilarejo de Malandeniya no distrito de Kurunegala no Sri Lanka. O seu preceptor foi o Venerável Kiribatkumbure Sonuttara Mahathera. Ele recebeu os ensinamentos básicos Budistas em uma pequena escola para monges e com 20 anos de idade recebeu a ordenação superior como monge em Kandy. Os estudos superiores foram realizados no Vidyalankara College em Kelaniya e no Missionary College da Mahabodhi Society em Colombo. Em seguida ele foi enviado para a Índia por 5 anos para um trabalho missionário através da Mahabodhi Society junto à casta dos intocáveis (Harijana) em Sanchi, Delhi e Bombaim. Mais tarde ele passou dez anos como missionário na Malásia onde atuou como conselheiro religioso da Sasana Abhivurdhiwardhana Society, Buddhist Missionary Society e da Buddhist Youth Federation of Malaysia. Ele atuou como professor nas escolas Kishon Dial School e na Temple Road Girl's School e como responsável pelo Buddhist Institute of Kuala Lumpur.<o:p></o:p></span></p>

<p class=Normal>A convite da Sasana Sevaka Society, Bhante Gunaratana foi para os Estados Unidos em 1968 para atuar como Secretário Geral do Buddhist Vihara Society em Washington, D.C. Em 1980 ele foi indicado como Presidente da sociedade. Durante esses anos no Vihara, ele ministrou cursos de Budismo, conduziu retiros de meditação e proferiu palestras em muitos lugares dos EUA e Canada. Ao mesmo tempo ele deu continuidade aos estudos acadêmicos e obteve o B.A., M.A. e Ph.D. em Filosofia da American University. Ele ministrou cursos de Budismo na American University, Georgetown University, Bucknell University e University of Maryland. Ele também proferiu palestras em muitas outras universidades nos EUA, Canada, Europa, Austrália e Nova Zelândia.<o:p></o:p></span></p>

<p class=Normal>Ele é o autor de <i>Come and See, The Path of Serenity and Insight, The Jhanas, Mindfulness in Plain English</i> (traduzido para o Português e publicado pela Edições Nalanda em 2003 como Meditação para Todos) e <i>Eight Mindfull Steps to Hapiness</i>, seu último livro em Inglês publicado em 2001. Seus artigos foram publicados nos EUA, Malásia, Índia, Sri Lanka, Inglaterra e França.<o:p></o:p></span></p>

<p class=Normal>Bhante Gunaratana foi o capelão Budista na American University aconselhando os estudantes interessados no Budismo e na prática de meditação. Atualmente ele é Presidente e Abade do Bhavana Society, um monastério da tradição das florestas, situado em West Virginia, EUA.  Ele continua a ensinar meditação e a conduzir retiros no Bhavana Society, onde reside, e como convidado em vários países espalhados pelo mundo. Ele também faz parte do corpo de professores visitantes do IMS – Insight Meditation Society em Barre/MA, EUA.<o:p></o:p></span></p>

<p class=Normal>Bhante Gunaratana já esteve várias vezes no Brasil conduzindo retiros de meditação, dando cursos e palestras em São Paulo e em Belo Horizonte.<o:p></o:p></span></p>

