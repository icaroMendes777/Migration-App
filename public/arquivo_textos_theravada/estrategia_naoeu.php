<p class=Normal>Os livros sobre o Budismo
com freqüência afirmam que um dos dogmas metafísicos mais básicos do Buda é que
não existe uma alma ou um eu. No entanto, uma pesquisa nos discursos do Cânone
em Pali - o registro existente mais antigo dos ensinamentos do Buda - sugere
que o Buda ensinou a doutrina de <i>anatta </i>ou não-eu, não como uma
afirmação metafísica, mas como uma estratégia para obter a libertação do
sofrimento. Se alguém usar o conceito de não-eu para não se identificar com os
fenômenos, irá ficar além do alcance de todo o sofrimento e estresse. Quanto
àquilo que se encontra além do sofrimento e estresse, o Cânone menciona que
embora possa ser experimentado, está além do alcance de uma descrição e por isso
tais descrições como &quot;eu&quot; ou &quot;não-eu&quot; não são aplicáveis.<o:p></o:p></span></p>

<p class=Normal>A evidência para esta
leitura do Cânone está concentrada em quatro pontos: <o:p></o:p></span></p>

<p class=Normal style=
margin-left:.75in'><b>1.</b> Numa
ocasião em que é feito um pedido direto ao Buda para que tome uma posição em
relação à questão ontológica, se existe ou não um eu, ele se recusa a
responder. <o:p></o:p></span></p>

<p class=Normal style=
margin-left:.75in'><b>2.</b>  Os
trechos que afirmam de forma mais categórica que não existe um eu estão
qualificados de tal maneira que eles cobrem toda a realidade que pode ser
descrita, mas não toda a realidade que pode ser experimentada. <o:p></o:p></span></p>

<p class=Normal style=
margin-left:.75in'><b>3.</b>  As idéias
de que não existe um eu estão niveladas com as idéias de que existe um eu como
uma &quot;cativeiro de idéias&quot; as quais uma pessoa que tem como objetivo a
libertação do sofrimento faria bem em evitar. <o:p></o:p></span></p>

<p class=Normal style=
margin-left:.75in'><b>4.</b>  A pessoa
que alcançou o objetivo da libertação vê a realidade de tal maneira que todas
as idéias - mesmo aquelas noções mais básicas como eu e não-eu, verdadeiro e
falso - não têm mais poder de influenciar a mente. <o:p></o:p></span></p>

<p class=Normal>O que segue é uma seleção
de trechos relevantes do Cânone. Eles são oferecidos com a advertência de que
em última análise nada conclusivo pode ser provado através da citação de
textos. Estudiosos têm apresentado argumentos para colocar em dúvida
praticamente tudo no Cânone - quer seja apresentando novas traduções para
termos cruciais ou questionando a autenticidade de praticamente cada trecho
contido nele - e dessa forma o único teste verdadeiro para qualquer
interpretação é colocá-la em prática e verificar aonde ela conduz em relação a
obter a libertação da mente. <o:p></o:p></span></p>

<p align=center style='text-align:center'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>* * *<o:p></o:p></span></p>

<p class=Normal><b>1.</span></b> Compare os dois diálogos a seguir. <o:p></o:p></span></p>


<p class=Normal style=
margin-left:.75in'>Tendo sentado a um lado, o errante Vacchagotta disse ao Abençoado,
'Então, Venerável Gotama, existe um eu?' Quando isso foi dito, o Abençoado
ficou em silêncio. 'Então, não existe um eu?' Uma segunda vez o Abençoado ficou
em silêncio. <o:p></o:p></span></p>


<p class=Normal style=
margin-left:.75in'>Então o errante Vacchagotta levantou-se do seu assento e partiu. <o:p></o:p></span></p>


<p class=Normal style=
margin-left:.75in'>Em seguida, não muito tempo após o errante Vacchagotta ter partido o
Venerável Ananda disse ao Abençoado, 'Porque, senhor, o Abençoado não respondeu
quando foi perguntado pelo errante Vacchagotta?' - 'Ananda, se eu, tendo sido
perguntado pelo errante Vacchagotta se existe um eu, tivesse respondido que
existe um eu, isso estaria conforme com aqueles brâmanes e contemplativos que
são os expoentes da doutrina eternalista (isto é, a idéia de que existe uma
alma eterna). E se eu…tivesse respondido que não existe um eu, isso estaria
conforme com aqueles brâmanes e contemplativos que são os expoentes do niilismo
(isto é de que a morte é a aniquilação da experiência). Se eu…tivesse respondido
que existe um eu, isso seria compatível com o surgimento do conhecimento de que
todos os fenômenos são não-eu? <o:p></o:p></span></p>


<p class=Normal style=
margin-left:.75in'>'Não, Senhor.' <o:p></o:p></span></p>


<p class=Normal style=
margin-left:.75in'>'E se eu…tivesse respondido que não existe um eu, o confuso Vacchagotta
ficaria ainda mais confuso: &quot;Aquele eu que eu costumava ter, agora não
existe?&quot;'<a href="../sutta/SNXLIV.10.php"> [SN XLIV.10]</a> <o:p></o:p></span></p>

 <p class=Normal>&nbsp;</p>

<p class=Normal style=
margin-left:.75in'><b>Mogharaja:</span></b><br>
"Como alguém deve ver o mundo de
modo a não ser visto pelo Senhor da Morte?” <o:p></o:p></span></p>

<p class=Normal style=
margin-left:.75in'><b>O Buda:</span></b><br>
“Mogaraja, sempre com atenção plena, desenraizando a idéia de uma
identidade, veja o mundo como vazio, assim você terá superado a morte. O Senhor
da morte não verá aquele que vê o mundo dessa forma.” <a href="../sutta/SnpV.15.php">[Snp V.15]</a><o:p></o:p></span></p>

<p class=Normal>O primeiro trecho é um dos
mais controversos no Cânone. Aqueles que acreditam que o Buda tomou uma posição
ou outra na questão sobre se existe ou não um eu têm que explicar o silêncio do
Buda e normalmente o fazem focando na sua declaração final para Ananda. Se uma
outra pessoa mais madura espiritualmente que Vacchagotta tivesse feito a
pergunta, eles dizem, o Buda teria revelado a sua verdadeira posição. <o:p></o:p></span></p>

<p class=Normal>Esta interpretação, no
entanto, ignora as duas primeiras sentenças do Buda para Ananda. Não importa
quem faça a pergunta, dizer que existe ou não existe um eu seria cair numa das
duas posições filosóficas que o Buda evitou durante toda a sua carreira. Quanto
à terceira sentença, ele estava preocupado em não contradizer &quot;o
surgimento do conhecimento de que todos os fenômenos são não-eu&quot; não
porque ele sentia que esse conhecimento em si era correto sob o ponto de vista
metafísico, mas porque ele viu que o seu surgimento poderia conduzir à
libertação. (Trataremos do conteúdo desse conhecimento no ponto 2 abaixo) <o:p></o:p></span></p>

<p class=Normal>Esse ponto é confirmado
quando fazemos uma comparação com o segundo discurso. A diferença fundamental
entre os dois diálogos está nas perguntas feitas: No primeiro, Vacchagotta pede
ao Buda que tome uma posição sobre a questão, se existe ou não um eu, e o Buda
fica em silêncio. Na segunda, Mogharaja pergunta sobre uma forma de ver o mundo
para que se possa superar a morte e o Buda responde ensinando-o a ver o mundo
sem relacioná-lo à noção de um eu. Isso sugere que, ao invés de ser uma
afirmação de que não existe um eu, o ensinamento sobre não-eu é mais uma
técnica de percepção que tem como objetivo conduzir para além da morte, Nibbana
- uma forma de perceber as coisas sem a iidentificação de  um eu, nenhuma noção
de que 'eu sou', nenhum apego a 'eu' ou 'meu'. <o:p></o:p></span></p>

<p class=Normal>Portanto, parece mais
honesto tomar o primeiro diálogo pelo que aparenta ser e dizer que a questão,
se existe ou não um eu, é algo sobre o qual o Buda não tomou uma posição, sem
levar em consideração se ele estava falando para uma pessoa confusa
espiritualmente como Vacchagotta ou uma pessoa mais avançada como Ananda. Para
ele, a doutrina do não-eu é uma técnica ou estratégia para a libertação e não
uma posição metafísica ou ontológica. <o:p></o:p></span></p>

<p align=center style='text-align:center'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>* * *<o:p></o:p></span></p>

<p class=Normal><b>2.</span></b> Os dois trechos a seguir, tomados
em conjunto, são com freqüência usados como a prova mais forte de que o Buda
negava a existência de um eu de forma muito ambígua. Note, no entanto, como os
termos &quot;mundo&quot; e &quot;Todo&quot; são definidos. <o:p></o:p></span></p>


<p class=Normal style=
margin-left:.75in'><b>Ananda:</span></b> <o:p></o:p></span></p>

<p class=Normal style=
margin-left:.75in'> “Venerável
senhor, dizem ‘o mundo está vazio, o mundo está vazio.’ Em relação a que dizem que
‘o mundo está vazio’?” <o:p></o:p></span></p>

<p class=Normal style=
margin-left:.75in'> “Ananda, porque está vazio de um eu ou
algo que pertença a um eu é que dizem, ‘O mundo está vazio.’ E o que é que está
vazio de um eu ou algo que pertença a um eu? O olho, Ananda, está vazio de um
eu ou algo que pertença a um eu. Formas...consciência no olho...Contato no olho
...Qualquer sensação que surja tendo o contato na mente como condição -
experimentada como prazer, dor ou nem prazer, nem dor - está vazio de um eu ou
algo que pertença a um eu. <o:p></o:p></span></p>

<p class=Normal style=
margin-left:.75in'> “Ananda,
é porque está vazio de um eu ou algo que pertença a um eu que dizem, ‘O mundo
está vazio.’” <a href="../sutta/SNXXXV.85.php"> [SN XXXV.85]</a><o:p></o:p></span></p>
<p class=Normal>&nbsp;</p>

<p class=Normal style=
margin-left:.75in'> “O que é o todo? Simplesmente, o olho e as formas, o ouvido e os sons, o
nariz e os aromas, a língua e os sabores, o corpo e os tangíveis, a mente e os
objetos mentais. Isso, bhikkhus é chamado o Todo. Qualquer um que diga,
‘Rejeitando esse todo, eu descreverei um outro,’ isso seria mera jactância por
parte dele. Se ele fosse questionado, seria incapaz de responder e além disso
ficaria aflito. Porque? Porque, bhikkhus, isso não estaria dentro do domínio
dele.”<a href="../sutta/SNXXXV.23.php"> [SN XXXV.23]</a><span style='mso-spacerun:yes'>  </span><o:p></o:p></span></p>



<p class=Normal>Agora, se os seis sentidos
e os seus objetos - algumas vezes denominados as seis bases do contato -
constituem o mundo ou o Todo, existe algo que esteja além deles? <o:p></o:p></span></p>

<p class=Normal style=
margin-left:.75in'><b>MahaKotthita:</span></b><br>
"Com a cessação e desaparecimento sem deixar vestígios das seis bases do contato
(visão, audição, olfato, sabor, corpo e mente) é o caso de que existe algo
mais? <o:p></o:p></span></p>

<p class=Normal style=
margin-left:.75in'><b>Sariputta:</span></b> <br>
"Não diga isso, meu amigo. <o:p></o:p></span></p>

<p class=Normal style=
margin-left:.75in'><b>MahaKotthita:</span></b><span lang=PT-BR style='mso-ansi-language:PT-BR'><br>
Com a cessação e desaparecimento sem deixar vestígios das seis bases do
contato, é o caso de que não existe algo mais?<o:p></o:p></span></p>

<p class=Normal style=
margin-left:.75in'><b>Sariputta:</span></b><span lang=PT-BR style='mso-ansi-language:PT-BR'><br>
Não diga isso, meu amigo. <o:p></o:p></span></p>

<p class=Normal style=
margin-left:.75in'><b>MahaKotthita:</span></b><span lang=PT-BR style='mso-ansi-language:PT-BR'><br>
... é o caso de ambos, existe e não existe algo mais? <o:p></o:p></span></p>

<p class=Normal style=
margin-left:.75in'><b>Sariputta:</span></b><span lang=PT-BR style='mso-ansi-language:PT-BR'><br>
Não diga isso, meu amigo. <o:p></o:p></span></p>

<p class=Normal style=
margin-left:.75in'><b>MahaKotthita:</span></b><span lang=PT-BR style='mso-ansi-language:PT-BR'><br>
... é o caso de que nem existe, nem não existe algo mais? <o:p></o:p></span></p>

<p class=Normal style=
margin-left:.75in'><b>Sariputta:</span></b><span lang=PT-BR style='mso-ansi-language:PT-BR'><br>
Não diga isso, meu amigo. <o:p></o:p></span></p>

<p class=Normal style=
margin-left:.75in'><b>MahaKotthita:</span></b><span lang=PT-BR style='mso-ansi-language:PT-BR'><br>
Sendo perguntado ... se existe algo mais, você diz 'Não diga isso, meu amigo'.
Sendo perguntado ... se não existe algo mais ... se ambos, existe e não existe algo
mais ... se nem existe, nem não existe, você diz, 'Não diga isso, meu amigo'.
Agora, como deve ser entendido o significado dessa afirmação? <o:p></o:p></span></p>

<p class=Normal style=
margin-left:.75in'><b>Sariputta:</span></b><span lang=PT-BR style='mso-ansi-language:PT-BR'><br>
Dizendo ... é o caso de que existe algo mais…é o caso de que não existe algo
mais…é o caso de ambos, existe e não existe algo mais ... é o caso de que nem
existe, nem não existe algo mais, está-se diferenciando a não diferenciação.
Até onde alcancem as seis bases do contato é até onde alcança a diferenciação.
Até onde alcance a diferenciação é até onde alcançam as seis bases do contato.
Com a cessação e desaparecimento sem deixar vestígio das seis bases do contato,
ocorre a cessação, o alívio da diferenciação. [<a href="../sutta/ANIV.174.php">AN IV.174</a>]<o:p></o:p></span></p>



<p class=Normal>A esfera da não
diferenciação, embora não possa ser descrita, pode ser compreendida através da
experiência direta. <o:p></o:p></span></p>

<p class=Normal style=
margin-left:.75in'>"Portanto, bhikkhus, essa base deve ser compreendida, na qual o olho cessa e a percepção das formas cessa. Essa base deve ser compreendida, na qual o ouvido cessa e a percepção dos sons cessa ... Essa base deve ser compreendida, na qual a mente cessa e a percepção dos objetos mentais cessa. Essa base deve ser compreendida." [<a href="../sutta/SNXXXV.117.php">SN XXXV.117</a>] <o:p></o:p></span></p>



<p class=Normal>Embora este último trecho
indique que existe uma esfera para ser experimentada além das seis esferas
sensuais, ela não deve ser tomada como um &quot;eu mais elevado&quot;. Este
ponto é colocado no <a href="../sutta/DN15.php">Grande Discurso da Causação</a>,
em que o Buda classifica todas as teorias do eu em quatro grandes categorias:
aquelas que descrevem um eu que é ou (a) dotado de forma (um corpo) e finito,
ou (b) dotado de forma e infinito; (c) sem forma e<span
style='mso-spacerun:yes'>  </span>finito; (d) sem forma e infinito. O texto não
exemplifica as várias categorias, mas podemos citar as seguintes como
ilustração: (a) teorias que negam a existência de uma alma e identificam o eu com
o corpo; (b) teorias que identificam o eu com todos os seres ou com o universo;
(c) teorias de almas discretas, individuais; (d) teorias de uma alma unitária
ou identidade imanente em todas as coisas. Ele a seguir rejeita todas essas
quatro categorias. <o:p></o:p></span></p>

<p class=Normal>Um outro trecho mencionado
com freqüência para mostrar que o Buda ensinou que não existe um eu é um verso
do Dhammapada, especialmente a terceira estrofe, em que a palavra dhamma se
refere tanto às coisas condicionadas como não condicionadas. Observe, no
entanto, o que o verso diz como um todo: esses insights são parte do caminho e
não o objetivo no final do caminho. <o:p></o:p></span></p>

<p class=Normal style=
margin-left:.75in'>'Todas as formações são impermanentes' - <br>
Quando alguém vê isso com discernimento<br>
e se desencanta do sofrimento,<br>
esse é o caminho para a purificação. <o:p></o:p></span></p>

<p class=Normal style=
margin-left:.75in'>'Todas as formações são insatisfatórias' - <br>
Quando alguém vê isso com discernimento<br>
e se desencanta do sofrimento,<br>
esse é o caminho para a purificação.<o:p></o:p></span></p>

<p class=Normal style=
margin-left:.75in'>'Todos os dhammas são não-eu' -<br>
Quando alguém vê isso com discernimento<br>
e se desencanta do sofrimento,<br>
esse é o caminho para a purificação. [<a href="../dhp/dhp20.php">Dhp 277-79</a>] <o:p></o:p></span></p>



<p class=Normal>Como veremos no trecho
abaixo, o Buda afirma que o meditador alcança a iluminação ao ver os limites de
todas as coisas condicionadas, ao ver o que está além delas e ao não se apegar a
nenhuma delas. No verso seguinte, o inquiridor do Buda se refere ao objetivo
como um dhamma, (fenômeno), enquanto o Buda o descreve como a remoção ou
eliminação de todos os dhammas - e dessa forma ele vai além de &quot;todos os
dhammas&quot; e qualquer afirmação possível que possa ser feita a seu respeito.
Uma vez que o meditador tenha realizado isso, nenhuma palavra - ser, não ser,
eu , não-eu – se aplica. <o:p></o:p></span></p>

<p class=Normal style=
margin-left:.75in'><b>Upasiva:<o:p></o:p></span></b></p>

<p class=Normal style=
margin-left:.75in'>"Aquele que chegou ao fim: ele é aniquilado, ou permanece eternamente intacto? Por favor, 
sábio, explique isso para mim pois esse fenômeno é do seu conhecimento.<o:p></o:p></span></p>


<p class=Normal style=
margin-left:.75in'><b>O Buda:<o:p></o:p></span></b></p>


<p class=Normal style=
margin-left:.75in'>"Não há nada através do qual se possa medir aquele que chegou ao fim. Aquilo através do qual alguém poderia defini-lo - não se aplica no caso dele. Quando todos os fenômenos são eliminados, todos os meios de definição também são eliminados. <a href="../sutta/SnpV6.php"> [Snp V.6]</a> <o:p></o:p></span></p>



<p align=center style='text-align:center'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>* * *<o:p></o:p></span></p>

<p class=Normal><b>3.</span></b> Embora o conceito
&quot;não-eu&quot; seja uma maneira útil de se desvencilhar das ligações e
apegos que conduzem ao sofrimento, a idéia de que não existe um eu é
simplesmente uma entre muitas idéias metafísicas ou ontológicas que atam as
pessoas ao sofrimento. <o:p></o:p></span></p>

<p class=Normal style=
margin-left:.75in'>"Neste caso, bhikkhus, uma pessoa comum sem instrução...não entende o
tipo de coisas que merecem atenção e que tipo de coisas não merecem atenção.
Assim sendo, ela se ocupa com aquelas coisas que não merecem atenção e não
se ocupa com as coisas que merecem atenção. É desta forma que ela se ocupa sem
sabedoria: ‘Eu existi no passado? Não existi no passado? O que fui no passado?
Como eu era no passado? Tendo sido que, no que me tornei no passado? Existirei
no futuro? Não existirei no futuro? O que serei no futuro? Como serei no
futuro? Tendo sido que, no que me tornarei no futuro?’ Ou então ela está no seu
íntimo perplexa acerca do presente: ‘Eu sou? Eu não sou? O que sou? Como sou?
De onde veio este ser? Para onde irá?’ <o:p></o:p></span></p>

<p class=Normal style=
margin-left:.75in'>Quando ela se ocupa dessa forma, sem sabedoria, uma entre seis idéias
surgem nela. A idéia de que ‘<i style='mso-bidi-font-style:normal'>o
eu existe para mim</i>’ surge como verdadeira e consagrada; <br>

ou a idéia de que ‘<i style='mso-bidi-font-style:normal'>o eu não existe
para mim’</i><span style='mso-spacerun:yes'>    <br>

ou a idéia de que ‘<i style='mso-bidi-font-style:normal'>eu percebo o eu
através do eu’</i> <br>

ou a idéia de que ‘<i style='mso-bidi-font-style:normal'>eu percebo o
não-eu através do eu’</i> <br>

ou a idéia de que ‘eu <i style='mso-bidi-font-style:normal'>percebo o eu
através do não-eu’</i> <br>

ou então ela tem uma idéia como esta: ‘<i style='mso-bidi-font-style:
normal'>É esse meu eu que fala e sente e experimenta aqui e ali o resultado de
boas e más ações; e esse meu eu é permanente, interminável, eterno, não sujeito
à mudança e<span style='mso-spacerun:yes'>  </span>irá durar tanto tempo quanto
a eternidade.</i>’<a name=R8></a><o:p></o:p></span></p>


<p class=Normal style=
margin-left:.75in'>A essa idéia especulativa, bhikkhus, se denomina um emaranhado de
idéias, uma confusão de idéias, idéias contorcidas, idéias vacilantes, idéias
que aprisionam. Presa por idéias que aprisionam, a pessoa comum sem instrução
não se vê livre do nascimento, envelhecimento e morte, da tristeza, lamentação,
dor, angústia e desespero; ela não se vê livre do sofrimento, eu digo.<o:p></o:p></span></p>

<p class=Normal style=
margin-left:.75in'>Um nobre discípulo bem instruído...entende quais são as coisas que
merecem atenção e quais coisas não merecem atenção. Sendo assim, ele não se ocupa
com as coisas que não merecem atenção,<span style='mso-spacerun:yes'> 
</span>ele se ocupa com as coisas que merecem atenção... Ele aplica sua atenção
com sabedoria: ‘<i style='mso-bidi-font-style:normal'>Isto é sofrimento’...</i>
‘<i style='mso-bidi-font-style:normal'>Esta é a origem do sofrimento’...</i> ‘<i
style='mso-bidi-font-style:normal'>Esta é a cessação do sofrimento’...</i> ‘<i
style='mso-bidi-font-style:normal'>Este é o caminho que conduz à cessação do
sofrimento’</i>. <a name=R9></a>Quando ele aplica a sua atenção com sabedoria
desta forma, três grilhões são abandonados: a idéia da existência de um eu, a
dúvida e o apego a preceitos e rituais.<a href="../sutta/MN2.php"> [MN 2]</a> <o:p></o:p></span></p>



<p align=center style='text-align:center'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>* * *<o:p></o:p></span></p>

<p class=Normal><b>4.</span></b> Portanto, embora a pessoa que
esteja no caminho precise fazer uso do Entendimento Correto, ele ou ela
transcendem todos os entendimentos ao alcançar o objetivo da libertação. Para
uma pessoa que atingiu o objetivo, a experiência ocorre sem que um 'sujeito' ou
'objeto' esteja sobreposto a esta, sem a interpretação da experiência ou
daquilo que é experimentado. O que existe é a experiência em si mesma. <o:p></o:p></span></p>

<p class=Normal style=
margin-left:.75in'>"Bhikkhus, tudo aquilo que no mundo, incluindo os seus devas, maras e
brahmas, esta população com seus contemplativos e brâmanes, seus príncipes e povo é<span style='mso-spacerun:yes'>  </span>visto, ouvido, sentido,
conscientizado, buscado, procurado, ponderado pela mente: tudo isso eu entendo.
Tudo aquilo que no mundo, incluindo os seus devas, maras e brahmas, esta
população com seus contemplativos e brâmanes, seus príncipes e o povo é visto,
ouvido, sentido, conscientizado, buscado, procurado, ponderado pela mente: tudo
isso eu compreendi completamente; tudo isso é do conhecimento do Tathagata, mas
o Tathagata não se estabelece nisso<o:p></o:p></span></p>

<p class=Normal style=
margin-left:.75in'>Portanto,
bhikkhus, ao ver aquilo que é para ser visto, o Tathagata não concebe<span
style='mso-spacerun:yes'>  </span>algo visível separado do objeto visto. Ele
não concebe o não visto. Ele não concebe um objeto digno de ser visto. Ele não
concebe aquele que vê. <o:p></o:p></span></p>

<p class=Normal style=
margin-left:.75in'>Ao ouvir … Ao sentir … Ao conscientizar aquilo que é para ser
conscientizado, o Tathagata não concebe algo conscientizável separado do objeto
conscientizado. Ele não concebe o não conscientizado. Ele não concebe um objeto
digno de se conscientizado. Ele não concebe aquele que conscientiza.<o:p></o:p></span></p>

<p class=Normal style=
margin-left:.75in'>Portanto, bhikkhus, o Tathagata, sendo o mesmo com relação a todos os
fenômenos que podem ser vistos, ouvidos, sentidos e conscientizados, é ‘Assim.’
E eu lhes digo: Não há nenhum outro ‘Assim’ mais elevado ou mais sublime.<o:p></o:p></span></p>


<p class=Normal style=
margin-left:.75in'> “Tudo aquilo que é visto, ouvido, sentido ou apegado,<br>

É estimado como
verdadeiro pelos outros,<br>

Entre aqueles entricheirados nas suas próprias idéias, <br>

Sendo ‘Assim’ eu não tomo nenhuma como verdadeira ou falsa.<o:p></o:p></span></p>


<p class=Normal style=
margin-left:.75in'>Tendo visto esse gancho muito antes,<br>

No qual gerações estão fisgadas, empaladas.<br>

 ‘Eu entendo, eu vejo, de fato é
assim!’ <br>

Não há nada que sujeite os Tathagatas.”<a href="../sutta/ANIV.24.php"> [AN IV.24]</a> <o:p></o:p></span></p>



<p class=Normal>Uma idéia é verdadeira ou
falsa apenas quando alguém julga com que precisão ela se refere a outra coisa.
Se alguém a considera simplesmente como uma afirmação, um evento em si mesmo,
verdadeiro ou falso já não se aplica. Portanto para o Tathagata que nas
experiências não impõe noções de sujeito e objeto e considera visões, sons,
sensações e pensamentos exclusivamente em si mesmos, as idéias não são nem
verdadeiras nem falsas, mas apenas fenômenos a serem experimentados. Sem a
noção de sujeito, não existe base para &quot;Eu sei, eu vejo&quot;, sem a noção
de objeto, não existe base para, &quot;Isto é exatamente como é&quot;. Idéias
sobre o verdadeiro, falso, eu, não-eu, etc., perdem dessa forma todo o seu
poder de controle e a mente fica livre para ser Tathata: intacta, sem estar
influenciada por nada de nenhuma forma. <o:p></o:p></span></p>

<p class=Normal style=
margin-left:.75in'>"Aqueles habilidosos (em julgar) dizem que (uma idéia se torna) um
grilhão se, apoiando-se nela, alguém considera todo o demais como inferior. <a href="../sutta/SnpIV.5.php"> [Snp IV.5]</a><o:p></o:p></span></p>
