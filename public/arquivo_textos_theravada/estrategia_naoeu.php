<p class=Normal style=
margin-left:.75in'> “Então
o Mestre Gotama não apóia nenhuma opinião especulativa?” <o:p></o:p></span></p>

<p class=Normal style=
margin-left:.75in'> “
Vaccha, ‘opinião especulativa’ é algo que o Tathagata colocou de lado. Pois o
Tathagata, Vaccha, viu isso: <a name=R3></a>‘Assim é a forma material, essa é
a sua origem, essa é a sua cessação; assim é a sensação, essa é a sua origem,
essa é a sua cessação; assim é a percepção ... assim são as formações volitivas ... assim é
a consciência, essa é a sua origem, essa é a sua cessação’. Então, eu digo,
com a destruição, desaparecimento, cessação, desistência e abandono de toda a
concepção de idéias, de todas as invenções, de todas as fabricações de um eu,
de todas as fabricações de um meu e da tendência subjacente para a presunção, o
Tathagata está libertado através do desapego.” <a href="../sutta/MN72.php"> [MN 72]</a><o:p></o:p></span></p>

