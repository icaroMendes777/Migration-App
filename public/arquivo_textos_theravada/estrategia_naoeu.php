<p class=Normal style=
margin-left:.75in'>Isto, bhikkhus, o Tathagata sabe: esses pontos de vista assim formulados
e agarrados irão conduzir a tal e qual destino num outro mundo. Isso o
Tathagata sabe, e ele sabe muito mais além disso, mas ele não tem apego por
esse conhecimento. E estando assim desatado ele experimenta a perfeita paz, e
tendo compreendido como na verdade é, a origem e a cessação das sensações, a
gratificação, o perigo e a escapatória das sensações, o Tathagata está
libertado sem restar nenhum resíduo.<a href="../sutta/DN1.php"> [DN 1]</a>  <o:p></o:p></span></p>



