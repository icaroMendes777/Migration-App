<p class=Normal>A questão que pretendo abordar neste breve ensaio é se os cinco meios dos sentidos estão ativos ou desativados nos <i>jhanas</i>. </p>

<p class=Normal>Não há um consenso entre os professores do Dhamma e os estudiosos com relação à resposta a essa questão. Para alguns, os sentidos são completamente desativados nos  <i>jhanas</i> e a mente permanece desconectada do corpo voltada completamente para si mesma, num estado de intenso prazer e alegria que gradualmente é substituído por um estado de absoluta paz e equanimidade. </p>

<p class=Normal>Para outros a mente desfruta das mesmas qualidades, no entanto não fica desconectada dos sentidos mas ao mesmo tempo não é perturbada por aquilo que impacta os sentidos. </p>

<p class=Normal>No primeiro caso a atividade de insight, ou investigação daquilo que está sendo experimentado, ocorre apenas ao sair dos estados de <i>jhana</i>. No segundo caso a atividade de insight pode ocorrer durante a experiência dos <i>jhanas</i>. </p>

<p class=Normal>É bom sempre lembrar que a característica fundamental dos <i>jhanas</i> é a mente desobstruída, ou seja sem ser importunada pelos <a href="../cinco_obstaculos.php">cinco obstáculos</a> que são: o desejo pelo prazer dos sentidos, a má vontade, a preguiça e o torpor, a inquietação e a ansiedade ou remorso, e a dúvida. No primeiro caso o insight é possível depois de sair dos <i>jhanas</i> pois durante algum tempo a mente ainda permanece desobstruída dos obstáculos. A mente desobstruída dos obstáculos é que permite ver com clareza a verdadeira natureza dos fenômenos. </p>

<p class=Normal>Não é difícil concluir que no primeiro caso, em que a mente se desconecta dos sentidos, a possibilidade da mente ser importunada pelos obstáculos é nula, visto que como regra geral os obstáculos têm origem na experiência dos sentidos. O prazer e a alegria experimentados nos <i>jhanas</i> são aquilo que asseguram a sua estabilidade ou seja é a intensidade dessas emoções que faz com que a mente permaneça estável nesses estados. </p>

<p class=Normal>Já no segundo caso, em que os sentidos permanecem ativos, o contato nos sentidos deve ser extremamente sutil de modo a não estimular os obstáculos. Com certeza esse tipo de estado pode ser experimentado, mas a questão principal é se esse estado pode ser mantido, ou seja se é um estado estável o suficiente, ou, se a qualquer momento, devido a algum estímulo sensorial mais impactante, esse estado possa ser perdido pois a mente será tomada por algum obstáculo. </p>
<br>

<p class=Normal><b>1. O que Dizem os Suttas:</b></p>

<p class=Normal>A definição padrão encontrada nos suttas para o primeiro <i>jhana</i>, seguindo a tradução para o inglês feita por Bhikkhu Bodhi, é a seguinte: </p>

<p class=Normal style='margin-left:1.0in'>"É o caso em que um bhikkhu afastado dos prazeres sensuais, afastado das qualidades não hábeis, entra e permanece no primeiro <i>jhana</i>, que é caracterizado pelo pensamento aplicado e sustentado, com o êxtase e felicidade nascidos do afastamento. </p>

<p class=Normal>Ajaan Brahmavamso oferece uma tradução um pouco distinta: </p>

<p class=Normal style='margin-left:1.0in'>"Um bhikkhu afastado dos cinco sentidos, afastado dos cinco obstáculos, entra e permanece no primeiro <i>jhana</i> que é caracterizado pelo movimento da mente e sustentação da mente no êxtase e felicidade nascidos do afastamento." </p>

<p class=Normal>Há três diferenças entre as duas traduções: </p>

<p class=Normal style='margin-left:1.0in'>i) afastado dos prazeres sensuais versus afastado dos cinco sentidos</p>

<p class=Normal>Os termos em pali são <i>vivicceva kamehi</i>. <i>Vivicceva</i> tem o significado de separar-se de algo, manter distância, afastar-se. <i>Kamehi</i> vem de <i>kama</i> que pode ter um significado objetivo ou subjetivo. O seu significado objetivo são os objetos dos sentidos, o significado subjetivo é o prazer e a alegria causados pelos objetos dos sentidos. </p> 

<p class=Normal>Numa troca de correspondência com Ajaan Brahm ele mencionou o seguinte: "Ajaan Brahmali provou com base nos suttas que sempre que a palavra <i>kama</i> é usada no plural significa os cinco sentidos. Portanto <i>vivicceva kamehi</i> sempre significa afastado/separado dos cinco sentidos."</p>

<p class=Normal>Portanto <i>vivicceva kamehi</i> tem o significado de separar-se, afastar-se, manter distância dos objetos dos sentidos e do prazer e alegria causado por esses objetos. </p>

<p class=Normal style='margin-left:1.0in'>ii) afastado das qualidades não hábeis vs. afastado dos cinco obstáculos</p>

<p class=Normal>Os termos em pali são <i>vivicca akusalehi dhammehi</i>. <i>Vivicca</i> já foi explicado acima. <i>Akusalehi dhammehi</i>, ou <i>akusala dhamma</i>, são qualidades ou fenômenos mentais prejudiciais ou inábeis. Os cinco obstáculos claramente se encaixam nessa definição. </p>

<p class=Normal style='margin-left:1.0in'>iii) caracterizado pelo pensamento aplicado e sustentado vs. caracterizado pelo movimento da mente e sustentação da mente</p>

<p class=Normal>Os termos em pali são <i>savitakkam savicaram</i>. <i>Vitakka e vicara</i> como regra geral são empregados com o sentido de pensamento, mas nos estados de meditação profunda, em que os pensamentos foram deixados de lado a interpretação como sendo movimento e sustentação da mente faz mais sentido. </p>

<p class=Normal>Portanto com base nessa análise dos termos em pali podemos oferecer uma tradução mais detalhada do primeiro <i>jhana</i> como: </p>

<p class=Normal style='margin-left:1.0in'>"Um bhikkhu, separado, afastado, mantendo à distância os objetos dos sentidos e o prazer e alegria causados pelos objetos dos sentidos; separado, afastado, mantendo à distância os obstáculos, que são qualidades inábeis e prejudiciais, entra e permanece no primeiro <i>jhana</i> que é caracterizado pelo movimento da mente e sustentação da mente no êxtase e felicidade nascidos do afastamento." </p>

<br>
<p class=Normal><b>2. Os suttas oferecem mais evidências que sustentem essa interpretação? </b></p>

<p class=Normal>Há alguns suttas do Anguttara Nikaya que oferecem algumas informações adicionais: </p>

<p class=Normal>O <a href="../sutta/ANIX.31.php">Anupubbanirodha Sutta</a>  diz o seguinte: </p>

<p class=Normal style='margin-left:1.0in'>"Bhikkhus, há essas nove cessações sucessivas. Quais nove? (1) Naquele que alcançou o primeiro <i>jhana</i>, a percepção sensorial cessa ..." </p>

<p class=Normal>Os termos em pali para "percepção sensorial cessa" são <i>kamasañña niruddha</i>. Tal como vimos acima <i>kama</i> pode ter um significado objetivo ou subjetivo. O seu significado objetivo são os objetos dos sentidos, o significado subjetivo é o prazer e a alegria causados pelos objetos dos sentidos. <i>Sañña</i> é percepção e <i>niruddha</i> é em geral interpretado como cessar, dissolver, desaparecer. No texto <a href="../arquivo_textos_theravada/origem_dependente_10.php#T4">Um Problema com a palavra <i>Nirodha</i></a> o Venerável Payutto explica que essa interpretação para a palavra <i>nirodha</i> apresenta alguns problemas, nas palavras do Venerável: </p>

<p class=Normal>"De modo geral, a palavra “cessar” significa eliminar algo que já havia surgido, ou de interromper algo que já havia começado. No entanto, <i>nirodha</i>, no ensinamento da Origem Dependente, (como também em <i>dukkhanirodha</i>, a terceira Nobre Verdade), significa o não surgimento ou não existência de algo porque a causa do seu surgimento foi eliminada. Por exemplo, a frase “quando <i>avijja</i> é <i>nirodha</i>, <i>sankhara</i> também é <i>nirodha</i>,” que em geral é interpretada como “com a cessação da ignorância, as formações volitivas cessam,” na verdade significa “quando não há ignorância, ou a ignorância não surge, ou quando não há mais qualquer problema com a ignorância, não existem formações volitivas, as formações volitivas não surgem, ou não existe mais nenhum problema com as formações volitivas.” Não significa que a ignorância que já surgiu tenha que ser eliminada antes que as formações volitivas que já surgiram também sejam eliminadas." </p>

<p class=Normal>Trazendo essa interpretação para <i>kamasañña niruddha</i> temos "a percepção dos objetos dos sentidos e do prazer e alegria causados pelos objetos dos sentidos não surge." Qual a condição para o surgimento da percepção? Justamente o contato, ou seja quando o contato não surge, a percepção não irá surgir. </p>

<p class=Normal>Essa interpretação ainda é mais explícita do que <i>vivicceva</i> que aparece na definição padrão do primeiro <i>jhana</i>. Combinando <i>vivicceva kamehi</i> e <i>kamasañña niruddha</i> temos que no primeiro <i>jhana</i> estamos falando de: </p>

<p class=Normal style='margin-left:1.0in'>"Separar-se, afastar-se, manter distância dos objetos dos sentidos e do prazer e alegria causado por esses objetos e, o não surgimento do contato e percepção dos objetos dos sentidos e do prazer e alegria causado por esses objetos." </p>

<p class=Normal>Existe alguma maneira mais clara de descrever o que acontece no primeiro <i>jhana</i> com os meios dos sentidos, com os objetos dos meios dos sentidos, e com as percepções causadas pelo encontro de ambos? </p>

<p class=Normal>No <a href="../sutta/ANIX.38.php">Lokayatika Sutta</a> o Buda descreve o mundo como os objetos do prazer sensual. Essa definição também é encontrada em outros suttas como por exemplo o <a href="../sutta/SNXII.44.php">Loka Sutta</a> em que a origem e a cessação do mundo têm como base os meios dos sentidos e os seus respectivos objetos. No Lokayatika Sutta é dito que um bhikkhu que está  no primeiro <i>jhana</i> "chega ao fim do mundo e permanece no fim do mundo", ou seja, esta é uma outra maneira de dizer que os sentidos estão inativos. </p>

<p class=Normal>O <a href="../sutta/ANV.176.php">Piti Sutta</a>  diz o seguinte: </p>

<p class=Normal style='margin-left:1.0in'>" ... Sempre que um nobre discípulo entra e permanece no êxtase do isolamento, nessa ocasião cinco coisas não lhe ocorrem. (1) Sofrimento e tristeza conectados com a sensualidade não lhe ocorrem. (2) Prazer e alegria conectados com a sensualidade não lhe ocorrem ... "</p>

<p class=Normal>O "êxtase do isolamento" significa o primeiro jhana. Sensualidade é <i>kama</i> em pali que como foi visto acima tem um sentido objetivo e subjetivo. O fato de não ocorrer o sofrimento/tristeza e felicidade/alegria conectados com a sensualidade também é um indicador que os sentidos estão desconectados.</p>

<br>
<p class=Normal><b>3. A Questão da Primeira Realização Imaterial</b></p>

<p class=Normal>A primeira realização imaterial ocorre depois do quarto <i>jhana</i>. A definição padrão da primeira realização imaterial encontrada em vários suttas é a seguinte: </p>

<p class=Normal style='margin-left:1.0in'>"Há seres que com a completa superação das percepções da forma, com o desaparecimento das percepções do contato sensorial, sem dar atenção às percepções da diversidade, [percebendo] o ‘espaço é infinito,’ pertencem à base do espaço infinito." </p>

<p class=Normal>Em pali:</p>

<p class=Normal style='margin-left:1.0in'><i>sabbaso rupasaññanam samatikkama patighasaññanam atthangama nanattasaññanam amanasikara ananto akasoti akasanañcayatanam upasampajja viharati</i></p>

<p class=Normal>Quero destacar dois termos nesse enunciado que são problemáticos:</p>

<p class=Normal><i>Patighasaññanam atthangama</i>: traduzido como "com o desaparecimento das percepções do contato sensorial".</p> 

<p class=Normal>De acordo com o dicionário pali, <i>patigha</i>  pode ter dois sentidos, o primeiro ético, significando repulsa, repugnância, ou raiva. Segundo, o sentido psicológico, significando reação dos sentidos. Essa foi a interpretação empregada por Bhikkhu Bodhi na tradução para o Inglês e também na tradução para o Português.</p> 

<p class=Normal>O antônimo, <i>apatigha</i> aparece nos textos com o significado de não criar obstáculos, não obstruir, não causar dano, com base nisso, Ajaan Thanissaro traduz <i>patigha</i> como resistência, oposição. Nesse caso a tradução ficaria "com o desaparecimento das percepções de resistência."</p>

<p class=Normal>A definição da primeira realização imaterial dá base aos argumentos que apenas nesse estado, e não já no primeiro <i>jhana</i>, haveria a superação da percepção da forma e do contato sensorial. </p>

<p class=Normal>Antes de verificar a evidência dos suttas é bom lembrar que o processo cognitivo na mente é descrito da mesma forma que nos demais meios dos sentidos, ou seja há o contato na mente e com base no contato ocorre a sensação e percepção. Sendo assim, a mente também opera como um meio do sentido, mas com a diferença que a mente não faz parte dos prazeres sensuais. No <a href="../sutta/MN13.php">Mahadukkhakkhandha Sutta</a> o Buda faz uma análise detalhada da gratificação, do perigo, e da escapatória dos prazeres sensuais sendo considerados apenas os cinco meios dos sentidos, excluindo a mente. </p>

<p class=Normal>Portanto se levarmos em conta o que foi dito antes, que os meios dos sentidos estão inativos nos <i>jhanas</i>, então podemos inferir que esta descrição da primeira realização imaterial se refere justamente ao contato na mente. </p> 

<p class=Normal>Há algum sutta que possa confirmar essa interpretação? </p>

<p class=Normal>No <a href="../sutta/MN128.php">Upakkilesa Sutta</a> o Buda dialoga com o venerável Anurudha e este relata a sua experiência de meditação desta forma: </p>

<p class=Normal style='margin-left:1.0in'> “Venerável senhor, enquanto assim permanecemos diligentes, ardentes e decididos, reconhecemos e vemos a luz e as formas. Depois a luz e as formas desaparecem, mas nós ainda não temos habilidade e compreendemos esses <i>nimittas</i>.” </p>

<p class=Normal>A frase "diligentes, ardentes e decididos" indica que o diálogo diz respeito à prática de meditação. Devemos tomar a luz mencionada como sendo a luz que surge com o primeiro <i>jhana</i>, o chamado <i>nimitta</i>. Mas o que seriam as formas neste caso? Visto que qualquer percepção de formas já deveria haver desaparecido ao entrar no primeiro <i>jhana</i>. </p>

<p class=Normal>A resposta pode ser encontrada no <a href="../sutta/ANVIII.64.php">Gayasisa Sutta</a> no qual o Buda diz o seguinte: </p>

<p class=Normal style='margin-left:1.0in'> “Bhikkhus, antes da minha iluminação, quando ainda era um Bodisatva não iluminado, eu percebia apenas a luz, mas não via as formas." </p>

<p class=Normal style='margin-left:1.0in'>"Eu pensei: 'Se perceber a luz e também ver as formas, nesse caso meu conhecimento e visão ficariam ainda mais purificados.' Assim numa outra ocasião, enquanto permanecia diligente, ardente, e decidido, percebi a luz e também vi as formas. No entanto eu não conheci aqueles devas, ou os cumprimentei, ou conversei com eles, e não obtive uma resposta deles." </p>

<p class=Normal>As formas mencionadas são os devas, ou seja não se trata da percepção das formas com base na faculdade da visão comum mas sim da percepção baseada num estado supra-humano possibilitada pelos <i>jhanas</i>. </p>

<p class=Normal>Regressando à primeira realização imaterial podemos concluir que a percepção das formas e do contato sensorial que desaparece nessa realização não se refere aos meios dos sentidos comuns mas sim à percepção e contato supra-humano que pode ocorrer nos <i>jhanas</i> com relação aos seres de outros planos de existência. </p>
<br>
<p class=Normal><b>4. Conclusão:</b></p>

<p class=Normal>Baseado nessa análise fica evidente que a conclusão mais lógica é que já à partir do primeiro <i>jhana</i> os meios dos sentidos estão inativos e a mente está completamente voltada e absorta em si mesma. </p>


