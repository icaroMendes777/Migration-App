<p class=Normal><a name="três_princípios"></a><b>Três Princípios</b> <a
href="#Top"><span style='text-decoration:none;text-underline:none'><img border=0 title="Topo" src="../img/tup.gif"></span></a>
</p>

<p class=Normal; align=right style='text-align:right'><i >6 Julho 1956&nbsp;&nbsp;&nbsp;&nbsp;</span></i><span
lang=PT-BR style='mso-ansi-language:PT-BR'><o:p></o:p></span></p>

<p class=Normal>Em resumo, existem três
princípios que são realmente básicos para a meditação: <o:p></o:p></span></p>

<p class=Normal><i style='mso-bidi-font-style:normal'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>1) Intenção Correta:</span></i><span lang=PT-BR style='mso-ansi-language:
PT-BR'> Você tem que decidir que irá se soltar de todos os pensamentos e
preocupações que dizem respeito ao mundo. Você não irá mantê-los para ficar
pensando neles. Cada pensamento e conceito que lida com o passado ou futuro é
um assunto mundano, e não do Dhamma. Decida-se que você irá fazer uma só coisa
agora: a tarefa da prática, e nada mais. Em outras palavras, você irá trabalhar
no presente imediato. A isto se<span style="mso-spacerun: yes">  </span>denomina
a intenção correta. <o:p></o:p></span></p>

<p class=Normal><i style='mso-bidi-font-style:normal'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2) O objeto correto:</span></i><span lang=PT-BR style='mso-ansi-language:
PT-BR'> Isto significa o tema correto ou ponto focal para a mente. O tema aqui
é <i style='mso-bidi-font-style:normal'>dhatuvavatthana,</i> ou determinação
dos elementos, um dos temas que toma o corpo como fundamento da meditação <i
style='mso-bidi-font-style:normal'>(kayanupassana-satipatthana).</i> Em resumo,
estaremos analisando os quatro elementos que compõem o corpo: os elementos da
terra, água, ar, e fogo. O elemento da terra abrange as partes duras do corpo,
como os ossos. O elemento da água abrange as partes líquidas, como a urina,
saliva, sangue e pus. O elemento do fogo abrange o calor e aquecimento no
corpo. O elemento do ar abrange as sensações de energia que fluem no corpo,
como a respiração. De todos esses elementos, o mais importante é o elemento do
ar, ou a respiração. Se outras partes do corpo são danificadas – digamos, se
você ficar cego, ou surdo, ou com os braços e pernas quebradas – você ainda
consegue sobreviver. Porém se não tiver nenhuma respiração, não poderá
sobreviver. Terá que morrer. Dessa forma a respiração é um objeto importante
porque forma a base da nossa consciência. <o:p></o:p></span></p>

<p class=Normal><i style='mso-bidi-font-style:normal'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>3) A qualidade correta: </span></i><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Isto significa as sensações de conforto ou desconforto que surgem no
corpo. Quando você cuida da inspiração e expiração de tal forma que ela flua
livremente através das várias partes do corpo, surgirão resultados. Observe
cuidadosamente se os resultados que o corpo e a mente colhem da respiração são
bons ou ruins. O corpo se sente livre e tranqüilo, ou ele se sente apertado e
contraído? A mente se sente calma, quieta, e prazerosa, ou ela está irritada,
distraída, e caótica? Se o corpo e a mente se sentem tranqüilos,<span
style="mso-spacerun: yes">  </span>isso conta como bons resultados. Se o oposto
é verdadeiro, então isso conta como resultados ruins. Dessa forma você tem que
ter a noção de como ajustar a respiração de tal forma que ela seja confortável.
<o:p></o:p></span></p>

<p class=Normal>Quanto às qualidades
corretas da mente, elas são a atenção plena e a plena consciência.<o:p></o:p></span></p>

<p class=Normal>Tente seguir esses três
princípios básicos cada vez que você praticar a concentração. Somente então
você obterá resultados que serão plenos e corretos. <o:p></o:p></span></p>

<p class=Normal>Quanto às recompensas da
concentração, existem muitas. Elas surgem de acordo com o poder da mente da
pessoa que está meditando, eu explicarei isso em uma outra ocasião.<o:p></o:p></span></p>


