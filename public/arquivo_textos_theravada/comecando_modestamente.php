<p class=Normal><a name="o_coração"></a><b>O Coração Elétrico </b> <a
href="#Top"><span style='text-decoration:none;text-underline:none'><img border=0 title="Topo" src="../img/tup.gif"></span></a>
<o:p></o:p></span></p>

<p class=Normal; align=right style='text-align:right'><i >22 Outubro 1958&nbsp;&nbsp;&nbsp;&nbsp;</span></i><span
lang=PT-BR style='mso-ansi-language:PT-BR'><o:p></o:p></span></p>

<p class=Normal>As correntes do coração são
rápidas, erráticas, e não assumem nenhuma forma que seja visível para o olho.
As correntes sonoras e do olfato podem ser medidas em termos numéricos – 1, 2,
3, 4, etc. – porém as correntes da mente não podem ser medidas. E é da natureza
das coisas que são rápidas que elas também sejam sutis. É por isso que as
correntes da mente são impossíveis de ser vistas por qualquer pessoa que não
esteja realmente interessada em investigá-las. As pessoas até afirmam que não
existe a mente em um individuo, que tudo que temos é um corpo, como as árvores.
Quando morremos, não sobra nada, nada para renascer. Existem somente os
elementos terra, água, ar, e fogo. <o:p></o:p></span></p>

<p class=Normal>É da natureza de coisas
realmente rápidas que não possamos vê-las – como elas são, como são seus
formatos ou suas características. Por exemplo, quando andamos em um carro ou
barco passando por outro que venha em direção contrária em alta velocidade, não
conseguimos ver os rostos das pessoas que estão no outro carro ou barco de
maneira<span style="mso-spacerun: yes">  </span>clara de modo a reconhecê-las.
Ou suponha que duas pessoas passem correndo uma pela outra em alta velocidade.
Elas não serão capazes de ver os rostos uma da outra. Alguns pássaros voam
pelos céus tão rápido que nem conseguimos vê-los. Tudo que podemos ouvir é o
ruido de quando eles cruzam o ar. Com as correntes da mente acontece o mesmo. <o:p></o:p></span></p>

<p class=Normal>O Buda descobriu que a
mente humana é algo poderoso – mais forte e mais divino do que qualquer coisa
que exista. Porém, porque a mente opera tão velozmente, não conseguimos vê-la.
Se quisermos vê-la, temos que fazê-la operar mais devagar. Conforme ela opera
mais devagar podemos fazer com que ela pare. Quando ela para, nos damos conta
de que a mente é algo verdadeiro, algo que não morre. Ao mesmo tempo, ela é
fresca. Quando ela ainda está em movimento, é quente. O calor vem do movimento.
Quando ela se movimenta com muita rapidez, pode gerar a eletricidade da cobiça,
raiva e delusão. <o:p></o:p></span></p>

<p class=Normal>Conforme geramos esses três
tipos de eletricidade dentro de nós, a mente sairá em disparada pelos seis
cabos – os nervos dos olhos, ouvidos, nariz, língua, corpo, e mente. Se
qualquer um desses cabos entrar em curto pode incendiar a nossa casa ou cidade.
Quando essas correntes se incendeiam na mente, elas podem desgastar os nervos
dos olhos, ouvidos, nariz, língua, e corpo, de tal forma que elas nos enviam a
informação errada e fazem com que nos equivoquemos. Se andamos por aí com cabos
expostos e encontramos alguém que também esteja com os cabos expostos,
criaremos um curto circuito, e ambos seremos destruídos. Já é suficientemente
mal que ambos estejamos gerando eletricidade, para piorar ainda mais as coisas,
colocamos as nossas mãos exatamente sobre os cabos expostos um do outro. Quando
isso acontece, somos eletrocutados. O perigo de cabos expostos é que a sua
corrente nos engula. Quando conectamos, o calor se acumula e acaba num incêndio.
<o:p></o:p></span></p>

<p class=Normal>O movimento da mente
acumula calor nos elementos do corpo, e quando os elementos ficam assim
desequilibrados, eles fazem surgir a dor e a enfermidade. Quando a mente se
movimenta dessa forma, ela obscurece tudo. Nossos olhos, ouvidos, etc. ficam
obscurecidos de forma que não conseguem ver a verdade das visões, sons, aromas,
sabores, tangíveis, e idéias. É por isso que o Buda nos ensina a despertar o
silêncio através do desenvolvimento de um lastro com as ações hábeis e
meritórias. E o que funciona como um lastro para as mentes? Somos ensinados a
criar lastro para a mente procurando por três grandes blocos de pedra: <i>generosidade,</i>
fazendo doações de coisas materiais; <i>virtude, </i>mantendo as nossas
palavras e atos dentro da normalidade; e <i>meditação, </i>treinando a mente.
Se a mente não se movimentar de forma mais lenta com o peso do que é hábil e
meritório, não há como conseguir alívio do calor do seu incêndio. Às vezes o
mal a puxa em uma direção, enquanto o bem a puxa em outra. A bondade é como a
corrente positiva; o mal, como a corrente negativa. A mente alterna entre o bem
e o mal, olhando para o bem somente de tempos em tempos, porém ela não encontra
a verdadeira paz e tranqüilidade.<span style="mso-spacerun: yes"> 
</span>Apesar disso, ela principia a ver as coisas com mais clareza, tal como
um carro que começa a reduzir a<span style="mso-spacerun: yes"> 
</span>velocidade porém ainda não parou.<o:p></o:p></span></p>

<p class=Normal>Assim precisamos encontrar
outras quatro maneiras de diminuir a velocidade da mente. Em outras palavras,
temos que nos assegurar que os nossos pensamentos, palavras e ações não caiam sob
a influência de quatro tipos de preconceitos: preconceitos baseados no desejo,
baseados na raiva, baseados na delusão e baseados no medo. Temos que ser
justos e gentis com outras pessoas, não causando dano a nós mesmos nem aos
outros. Isso ajuda a nossa mente a se mover cada vez mais lentamente. Se ela se
move para a frente, tem quatro bloqueios no caminho. Se move para trás, tem
três pedras segurando. Necessitamos de princípios para o modo como sentamos,
ficamos parados, caminhamos, deitamos, falamos, agimos, etc. Esses são os
suportes da mente no desenvolvimento da tranqüilidade e insight. Isso é o que
significa meditação. <o:p></o:p></span></p>

<p class=Normal>A mente é como uma máquina.
Quando uma máquina para, podemos com segurança tocar as suas correias e
engrenagens. As correias neste caso são os seus vários conceitos e percepções.
Em outras palavras, percepções do passado e futuro giram para trás e para a
frente, é por isso que a mente não consegue esfriar. Enquanto se mantem
girando, gera calor. Se ela gira realmente rápido, irá pegar fogo, queimando a
si mesma e se espalhando para queimar outras pessoas também. É por isso que
somos ensinados a parar o giro cortando as correias. Em outras palavras,
praticamos a meditação da tranqüilidade, não permitindo que a mente saia
girando atrás das correntes do mundo. Qualquer que seja a atividade em que você
esteja envolvido, mantenha a mente totalmente envolvida no que estiver fazendo.
É como a água no oceano quando está cheio de ondas: se tomamos uma tigela de
água e a colocamos de lado até que as ondas se acalmem, ou se clareamos a água
com um cristal de alume, podemos olhar para a água e ver com clareza o nosso
reflexo. <o:p></o:p></span></p>

<p class=Normal>Nosso rosto é algo que
normalmente nunca vemos. Embora usemos a nossa boca para falar todos os dias,
nunca vimos que formato ela tem. Embora respiremos através do nosso nariz a
cada momento, nunca o vemos. Nossos ouvidos ouvem sons o dia todo, porém nunca
vemos a sua aparência. Nossos olhos podem ver todo tipo de coisas, porém eles
não conseguem ver a si mesmos. É por isso que dependemos de espelhos para ver o
nosso reflexo. Somente então poderemos ver o nosso rosto. Quando as pessoas têm
sabedoria, é como se elas tivessem um grande espelho para olhar para si
mesmas, porque a sabedoria é o conhecimento claro que surge de uma mente
luminosa, limpa, e pura, livre do girar, livre de ondas. <o:p></o:p></span></p>

<p class=Normal>Quando a mente para de
girar, ela chega ao silêncio. Esse silêncio é o que dá origem à sabedoria
que se desenvolve em habilidades cognitivas dentro de nós – as três e as oito
habilidades, tal como a lembrança de vidas passadas, que nos permite ver a nós
mesmos; conhecimento do falecimento e surgimento dos seres vivos – uma vez que
tenhamos visto o suficiente a nosso respeito, podemos ver outras pessoas; e o
conhecimento do fim das impurezas mentais: podemos ver o que é bom, o que é
mal, o que deveria ser abandonado, o que deve ser desenvolvido, o que nasce e
morre, o que não nasce e não morre. Quando essas habilidades surgirem dentro de
nós, compreenderemos completamente nossos corpos e mentes, bem como as coisas
fabricadas em geral. Os três fogos da cobiça, raqiva e delusão ficarão
separados, distantes do coração. O coração para de girar velozmente, e quando
não gira mais, o fogo e a eletricidade param, deixando nada além de frescor
e<span style="mso-spacerun: yes">  </span>tranqüilidade. <o:p></o:p></span></p>

<p class=Normal>É por isso que somos
ensinados a encontrar lastro e bloqueios para a mente de forma que ela gire
cada vez mais lentamente, até que pare, com o propósito do frescor,
tranqüilidade, e paz que mencionei antes. <o:p></o:p></span></p>


