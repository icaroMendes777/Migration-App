<p class=Normal><a name=cobras></a><b>Cobras, Fogos, e Ladrões </b> <a
href="#Top"><span style='text-decoration:none;text-underline:none'><img border=0 title="Topo" src="../img/tup.gif"></span></a>
<o:p></o:p></span></p>

<p class=Normal; align=right style='text-align:right'><i >22 Abril 1959&nbsp;&nbsp;&nbsp;&nbsp;</span></i><span
lang=PT-BR style='mso-ansi-language:PT-BR'><o:p></o:p></span></p>

<p class=Normal>Os perigos que a mente
enfrenta são como cobras venenosas, fogos, e grandes ladrões – coisas que estão
sempre à espreita para nos destruir: roubando-nos, matando-nos, e despindo-nos
dos nossos valores, nossa bondade humana, todos os dias e noites. <o:p></o:p></span></p>

<p class=Normal>“Cobras venenosas” neste
caso significa a cobiça, raiva e delusão, que possuem venenos doloridos que se
infiltram nas mentes das pessoas comuns. Quando chega ao coração esses venenos
podem matá-las.<o:p></o:p></span></p>

<p class=Normal>Quanto aos “fogos”, existem
dois tipos: fogos na floresta e fogos em casas. Um fogo na floresta não possui
nenhum único dono. Surge<span style="mso-spacerun: yes"> 
</span>espontaneamente, por sua natureza, e espalha sua destruição em uma
grande área, sem fronteiras, até que morra por si mesmo. Isto significa os
fogos do nascimento, envelhecimento, enfermidade e morte, formas de sofrimento
que surgem no corpo de todos seres vivos. Esse fogo pode queimar ambos nossos
tesouros mundanos e nossos tesouros nobres (isto é, a bondade da mente que de
outra forma poderíamos desenvolver). Quanto aos fogos em casas, esses são fogos
que surgem de dentro do coração – contaminações, ignorância, desejo e apego –
os obstáculos que<span style="mso-spacerun: yes">  </span>se interpõem no
caminho da bondade que vem do treinamento do coração e mente. <o:p></o:p></span></p>

<p class=Normal>Os “grandes ladrões” ou “os
500 criminosos mais procurados” significam os cinco agregados: forma, sensação,
percepção, formações mentais e consciência, que estão constantemente nos
roubando, nos matando, e nos oprimindo, destruindo ambos, nossos tesouros
mundanos e nobres. Além disso, existem os criminosos subterrâneos que
sorrateiramente se aproximam de nós sem nos darmos conta: ganho material,
fama, elogios e alegria por coisas externas. Quem quer que seja ludibriado por
esses criminosos terá dificuldades em libertar-se. É por isso que eles podem
destruir a bondade que de outra forma poderíamos alcançar no coração e mente. <o:p></o:p></span></p>

<p class=Normal>Todas essas cobras
venenosas, fogos e criminosos oferecem um tremendo perigo para o coração. Eles
ficam destruindo a nossa bondade a cada instante. Se não temos sabedoria em
relação a eles, teremos dificuldade em libertarmo-nos deles. A única forma de
prevenir esses perigos é através do poder do Dhamma: em outras palavras, a
prática da meditação, usando nossos poderes do pensamento aplicado e sustentado
até o ponto em que faremos surgir a sabedoria que claramente sabe e vê a
verdade de todas as coisas fabricadas. Quando pudermos ver os perigos em todos
os lados, aprenderemos a ser cuidadosos e manter a guarda, buscar formas de
destruí-los ou escapar deles. Quando conseguirmos fazer isso, nossas vidas
serão felizes. <o:p></o:p></span></p>

<p class=Normal>Quando praticamos o Dhamma
é como se estivéssemos indo através de uma floresta deserta, desolada, a
caminho do objetivo que é a mais elevada forma de felicidade e segurança. Para
atravessar a floresta, temos que depender da prática da concentração, com a
nossa atenção plena circunspecta em todos os lados. Não podemos ser negligentes
ou complacentes. Precisamos fazer o esforço para eliminar todos os conceitos e
preocupações que surgem para destruir a bondade da mente. Quando sabemos que há
cobras venenosas, fogos e os 500 criminosos mais procurados esperando por nós
ao longo do caminho, temos que estar plenamente atentos, vigilantes e despertos
todo o tempo, e ter boas armas preparadas para que possamos vencê-los. <o:p></o:p></span></p>

<p class=Normal>Ao mesmo tempo,
necessitamos provisões para nos auxiliar no caminho – em outras palavras os fatores
de <i style='mso-bidi-font-style:normal'>jhana.</i> Pensamento aplicado é o que
foca a mente naquilo que ela quer conhecer. Pensamento sustentado é o que mata
os obstáculos. Essas duas qualidades são como preparar o jantar. Porém se
somente temos essas duas qualidades, é como se tivéssemos preparado nosso
jantar mas sem saber o sabor dos diferentes tipos de comida que temos. Se
podemos acalmar a mente até que ela seja uma só com o seu objeto, isso é como
comer e engolir a nossa comida. Então saberemos o seu sabor e através dele
seremos capazes de ter a sensação de saciedade e nutrimento: em outras
palavras, uma sensação de êxtase, felicidade e unicidade da mente. O coração
então será capaz de ganhar força total, tal como o corpo quando tem uma
refeição nutritiva. <o:p></o:p></span></p>

<p class=Normal>A comida externa é o que
alimenta o corpo e lhe dá força. Quando o corpo tem força, podemos caminhar e
correr para onde queiramos. O que quer que seja que queiramos fazer, teremos as
forças para ter sucesso. Quanto à comida interna – o Dhamma – isso é o que
alimenta o coração e a mente. Quando o coração e a mente estão bem alimentados,
o poder do coração se torna resistente e forte. No que quer que seja que
apliquemos a nossa mente seremos bem sucedidos em relação aos nossos
pensamentos. Se a mente está privada do alimento do Dhamma, ela fica débil e
fraca. Os pensamentos não são bem sucedidos, ou na melhor hipótese, terá
sucesso em algumas coisas porém não em outras, não exatamente de acordo com as
nossas expectativas. É por isso que temos que reforçar a força das nossas
mentes o máximo que pudermos, pois a força da mente é a coisa mais importante
dentro de nós que nos levará ao objetivo da máxima felicidade. <o:p></o:p></span></p>

<p class=Normal>Enquanto você ainda estiver
vivo e respirando, não permita que você<span style="mso-spacerun: yes"> 
</span>seja negligente ou complacente. Não permita que o tempo passe sem nenhum
propósito. Apresse-se e acelere seus esforços no desenvolvimento da bondade –
pois quando não houver mais respiração, não haverá mais oportunidade de fazer o
bem... <o:p></o:p></span></p>

<p class=Normal>Você deve focar
exclusivamente em qualquer pensamento que auxilie a fazer com que a mente fique
firme, de forma a que possa fazer surgir a bondade. Não flerte com outros tipos
de pensamento, não importando se eles pareçam mais ou menos sofisticados.
Coloque-os para fora. Não os traga para a mente para pensar a seu respeito.
Mantenha a mente firmemente colocada em uma única preocupação: esse é o seu
verdadeiro coração, o verdadeiro coração dos ensinamentos do Buda. <o:p></o:p></span></p>


