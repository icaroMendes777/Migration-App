<p class=Normal>A palavra "satipatthana" é o nome de um método de meditação que tem como objetivo estabelecer sati, ou atenção plena. O termo sati está relacionado com o verbo sarati, recordar-se ou manter na mente. Sati algumas vezes é traduzido como atenção sem reação, livre de uma agenda, simplesmente estar presente com o que quer que surja, mas a fórmula de satipatthana não dá suporte a essa tradução. A atenção sem reação é na verdade um aspecto da equanimidade, uma qualidade fomentada no processo de  satipatthana. A atividade de satipatthana, no entanto, definitivamente tem uma agenda que a motiva: o desejo pela iluminação, que é classificado não como uma causa do sofrimento, mas como parte do caminho que lhe dá fim (veja o <a href="../sutta/SNLI.15.php">SN 51.15</a>). O papel da atenção plena é manter a mente estabelecida no momento presente de um modo que a mantenha no caminho. Fazendo uma analogia, a iluminação é como uma montanha no horizonte, o destino para o qual você está dirigindo um carro. A atenção plena é aquilo que faz com que a atenção permaneça focada na estrada para a montanha ao invés de permitir que foque apenas em vislumbres da montanha, ou seja distraída por outros caminhos que se desviam da estrada. </p>

 

<p class=Normal>Sendo um termo composto, satipatthana pode ser desmembrado de duas formas, sati-patthana, fundamento da atenção plena; ou sati-upatthana, estabelecimento da atenção plena. Os estudiosos debatem sobre qual é a interpretação mais apropriada, mas na prática ambas proporcionam bom material para reflexão. </p>

 

<p class=Normal>A primeira interpretação foca nos objetos da prática de meditação, os pontos focais que proporcionam um fundamento para a atenção plena. No total há quatro objetos: o corpo como corpo; as sensações como sensações; a mente como mente; os objetos mentais como objetos mentais. O “como” neste caso é crucial. No caso do corpo, por exemplo, significa encarar o corpo em si mesmo ao invés de vê-lo sob a perspectiva da sua função no contexto do mundo (pois nesse caso o mundo seria o fundamento). Abandonando toda preocupação sobre como a beleza, agilidade ou força do corpo se encaixam no mundo, o meditador simplesmente permanece com a experiência direta da respiração, dos movimentos do corpo, das suas posturas, das suas propriedades elementares e da sua inevitável decadência. Um princípio semelhante se aplica aos demais fundamentos. </p>

 

<p class=Normal>A segunda interpretação de satipatthana — sati-upatthana — foca no processo da prática meditativa, o modo como um fundamento é estabelecido. Este sutta proporciona três estágios nesse processo, aplicados a cada fundamento. O primeiro estágio da prática de satipatthana, do modo aplicado ao corpo é o seguinte: </p>

 

<p class=Normal>"Um bhikkhu permanece contemplando o corpo como um corpo, ardente, plenamente consciente e com atenção plena, tendo colocado de lado a cobiça e o desprazer pelo mundo." </p>

 

<p class=Normal>"Permanece contemplando" se refere ao elemento da concentração na prática, o meditador mantendo-se com o fundamento em meio às correntes conflitantes das experiências. “Ardente” significa o esforço que é feito na prática, para abandonar os estados mentais inábeis e para desenvolver os estados mentais hábeis no seu lugar, ao mesmo tempo em que há o discernimento da diferença entre os dois. “Plena consciência” significa estar totalmente ciente daquilo que está acontecendo no presente. “Atenção plena,” da forma como foi mencionada acima significa ser capaz de manter o fundamento continuamente na mente. Todas essas qualidades operam em conjunto, elas conduzem a mente para um estado de sólida concentração. Embora com freqüência seja dito que a prática de satipatthana é distinta da prática de jhana, um número de suttas – como por exemplo o <a href="../sutta/MN125.php">MN 125</a> e o <a href="../sutta/ANVIII.63.php">AN VIII.63</a> — igualam a finalização bem sucedida do primeiro estágio de satipatthana com a realização do primeiro nível de jhana. Este ponto é confirmado em muito suttas - entre eles o <a href="../sutta/MN118.php">MN 118</a> – descrevendo como a prática de satipatthana conduz à realização dos fatores da iluminação, que coincidem com os fatores de jhana. </p>

 

<p class=Normal>O segundo estágio da prática de satipatthana é o seguinte: </p>

 

<p class=Normal>"Ele permanece contemplando fenômenos que surgem no corpo, ou ele permanece contemplando fenômenos que desaparecem no corpo, ou ele permanece contemplando ambos, fenômenos que surgem e fenômenos que desaparecem no corpo." </p>

 

<p class=Normal>Os “fenômenos que surgem e desaparecem” abrangem eventos direta ou indiretamente relacionados com o fundamento escolhido. “Diretamente” significa mudanças no próprio fundamento. Por exemplo, ao focar no corpo, o meditador poderá observar o surgimento e o desaparecimento de sensações no corpo provenientes da respiração. “Indiretamente” neste caso significa eventos em qualquer um dos outros três fundamentos na medida em que se relacionam ao corpo. Por exemplo, o meditador poderá observar o surgimento e desaparecimento de sensações de prazer ou estados de irritação mental relacionados com eventos no corpo. Ou o meditador poderá notar lapsos de atenção plena ao estar focando no corpo. </p>

 

<p class=Normal>Em cada um desses casos se o surgimento e o desaparecimento forem eventos neutros como por exemplo os agregados, o meditador é orientado para apenas ter consciência disso como eventos, permitindo que eles sigam o seu curso natural de modo a observar quais fatores os acompanham e que conduzem ao seu vir e ir. No entanto, quando as qualidades mentais hábeis ou inábeis – como por exemplo os fatores da iluminação ou os obstáculos – surgem e desaparecem, o meditador é encorajado a fomentar os fatores que fortalecem os jhanas e eliminar os fatores que os enfraquecem. Isso significa estar ativamente envolvido em maximizar as qualidades mentais hábeis e minimizar as inábeis. Dessa forma o meditador desenvolve o insight do processo de origem e cessação assumindo um papel ativo e perceptivo no processo, da mesma forma como passamos a entender mais de ovos usando-os na culinária, obtendo experiência dos êxitos e fracassos ao tentar pratos cada vez mais sofisticados. </p>

 

<p class=Normal>Na medida em que esse processo conduza a estados cada vez mais refinados de concentração, isso faz com que o meditador se torne cada vez mais sensível ao fato que quanto mais grosseiras forem as suas participações no processo mental de surgimento e desaparecimento, mais grosseiro será o nível de sofrimento resultante. Isso conduz o meditador a abandonar níveis cada vez mais refinados de participação na medida em que ele seja capaz de detectá-los, conduzindo ao terceiro e último estágio na prática de satipatthana: </p>

 

<p class=Normal>"Ou então, a atenção plena de que ‘existe um corpo’ se estabelece somente na medida necessária para o conhecimento e para a continuidade da atenção plena. E ele permanece independente, sem nenhum apego a qualquer coisa mundana." </p>

 

<p class=Normal>Este estágio corresponde a um modo de percepção que o Buda descreve no <a href="../sutta/MN121.php">MN 121</a> como a “entrada no vazio”: </p>

 

<p class=Normal>"Assim, ele considera como vazio aquilo que não está ali, mas com relação àquilo que permanece ali ele compreende da seguinte forma: 'Isto está presente.'"</p>

 

<p class=Normal>Esse é o equilíbrio culminante no qual o caminho da prática se abre para um estado de não fabricação e disso para o fruto da iluminação e libertação. </p>

 

<p class=Normal>À primeira vista os quatro fundamentos para a prática de satipatthana parecem ser quatro diferentes exercícios de meditação, mas o <a href="../sutta/MN118.php">MN 118</a> deixa claro que todos eles podem estar centrados numa única prática: manter a mente na respiração. Quando a mente está com a respiração, todos os quatro fundamentos estão exatamente ali. A diferença está simplesmente na sutileza do foco do meditador. É como aprender a tocar piano. Na medida em que a prática melhora, você também fica mais sensível e capaz de ouvir níveis cada vez mais sutis na melodia. Isso possibilita que você toque com mais habilidade ainda. Do mesmo modo, na medida em que um meditador se torna mais hábil em permanecer com a respiração, a prática de satipatthana proporciona mais sensibilidade para expor níveis cada vez mais sutis de envolvimento no momento presente até que nada mais reste interferindo no caminho da completa libertação. </p>

 

 

