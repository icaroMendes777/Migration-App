<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-language:PT-BR'>Uma montanha é pesada?<br>

Ela pode ser pesada, mas contanto que nós não<br>

tentemos levantá-la, ela não será pesada para
nós.<o:p></o:p></span></p>

<p class=Normal> Essa é uma metáfora que um dos meus professores, Ajaan
Suwat,<span style="mso-spacerun: yes">  </span>freqüentemente usava para
explicar como parar o sofrimento decorrente dos problemas da vida. Você não
nega a sua existência – as montanhas são pesadas – e você não foge delas. Como
ele acrescentaria, você lida com os problemas sempre que necessário e os
resolve até onde for possível. Você simplesmente aprende como não carregá-los
por aí. A arte da prática está em viver com problemas reais sem fazer da
realidade deles um fardo para o coração.<o:p></o:p></span></h3>

<p class=Normal>Como um primeiro passo para dominar essa
arte, seria bom dar uma olhada na fonte usada por Ajaan Suwat para a construção
da sua metáfora – os ensinamentos do Buda sobre<span style="mso-spacerun:
yes">  </span>dukkha<span style="mso-spacerun: yes">  </span>- para termos uma
idéia da extensão da metáfora.<o:p></o:p></span></p>

<p class=Normal>Dukkha é uma palavra notoriamente difícil de traduzir para o Português.
No Cânone em Pali, ela se aplica tanto à dor e ao desconforto físico como à dor
e à enfermidade mental, variando desde a angústia intensa até a noção mais
sutil de opressão ou confinamento. Os comentários em Pali explicam dukkha como
“aquilo que é difícil de suportar”. Ajaan Maha Boowa, um mestre Tailandês da
tradição de florestas, a traduz como “qualquer coisa que coloque um aperto no
coração”. Embora nenhuma palavra em Português cubra todos esses sentidos, a
palavra estresse – como uma tensão no corpo ou na mente – parece a mais próxima
do termo em Pali. É assim que traduzirei essa palavra neste artigo, embora
também venha a usar a palavra sofrimento, quando a palavra estresse parecer
muito suave.<o:p></o:p></span></p>

<p class=Normal>O Buda
focou seus ensinamentos na questão do estresse porque ele<span
style="mso-spacerun: yes">  </span>encontrou um método para transcendê-lo. Para
entender esse método, temos que ver que partes da nossa experiência estão
marcadas pelo estresse. A partir dessa perspectiva, a experiência cai em duas
grandes categorias: condicionada <i>(sankhata)</i> – que ocorre a partir de
forças<span style="mso-spacerun: yes">  </span>e processos causais - e não
condicionada <i>(asankhata)</i>. Toda experiência comum é condicionada. Mesmo
um simples ato de olhar para uma flor é condicionado, naquilo que depende de
condições físicas que dão uma base para a existência da flor, junto com todos
os complexos fatores físicos e mentais envolvidos no ato de ver.<span
style="mso-spacerun: yes">  </span>A única experiência que não é condicionada é
extraordinária – nibbana – pois não depende de fatores causais de nenhum tipo.</span></p>

<p class=Normal>O estresse
está totalmente ausente na experiência não condicionada. Sua relação com a
experiência condicionada, no entanto, é mais complexa. Quando o Buda falou
sobre Dukkha em relação às três características comuns - impermanência,
estresse e não-eu - ele disse que todas as experiências condicionadas são por
natureza repletas de estresse. Sob esse ponto de vista, mesmo o olhar para uma
flor é cheio de estresse, apesar do óbvio prazer que isso traz, pois ele se
apóia numa frágil combinação de fatores que juntos compõem a experiência. Assim,
está claro que se nós quisermos ir além do estresse nós teremos que ir além da
experiência condicionada. Mas isso apresenta um problema: o que usaremos para
alcançar o não condicionado? Nós não podemos usar a experiência não
condicionada para levar-nos até lá, pois – por definição – ela não pode desempenhar
um papel em nenhum processo condicionado. Ela não pode ser usada como uma
ferramenta. Assim, necessitamos de um método no qual a experiência condicionada
seja usada com a finalidade de transcender-se a si mesma.</span></p>

<p class=Normal>Para
satisfazer essa necessidade, o Buda falou sobre dukkha em um outro contexto: as
Quatro Nobres Verdades. Aqui, por razões de estratégia, ele dividiu a
experiência condicionada em três verdades – estresse, sua causa (desejo), e o
caminho para a sua cessação (o Nobre Caminho Óctuplo). A experiência não
condicionada ele deixou como a verdade restante: a cessação do estresse. Ao
definir a primeira verdade ele disse que as experiências condicionadas eram
cheias de estresse <b><i>apenas</i></b> quando
acompanhadas pelo apego. Nesse sentido, olhar para a flor não é estresse, a
menos que nos apeguemos à experiência e tentemos basear a nossa felicidade
nisso.</span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Então é óbvio que nesses dois contextos o Buda está falando de dukkha
com dois sentidos diferentes. A metáfora da montanha de Ajaan Suwat ajuda a
explicar como esses dois sentidos estão relacionados.O peso da montanha
significa dukkha como uma das três características comuns: o estresse inerente
em todas as experiências condicionadas.<span style="mso-spacerun: yes"> 
</span>O fato da montanha ser pesada apenas para aqueles que tentam levantá-la
significa dukkha como uma nobre verdade: o estresse que vem apenas com o apego
- o apego que transforma a dor física em dor mental e torrna o envelhecimento, enfermidade
e morte um sofrimento mental.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>O Buda ensinou dukkha como uma das três características comuns para nos
fazer refletir sobre as coisas às quais nos apegamos: vale mesmo a pena nos
agarrarmos a elas? Se a resposta é não, porque então continuamos nos apegando?
Se a vida não oferecesse prazeres melhores do que aqueles que já temos
resultantes do nosso apego, a insistência do Buda, no estresse contido em
coisas como olhar para uma flor, poderia parecer grosseira e negativa. Mas o
seu propósito em fazer com que reflitamos sobre o outro lado dos prazeres
comuns é para abrir nossos corações para algo muito positivo: a mais alta forma
de felicidade, totalmente<span style="mso-spacerun: yes">  </span>destituída de
sofrimento e estresse, que vem apenas quando nos soltamos de tudo
completamente. Assim, ele também ensinou dukkha como uma nobre verdade para
focar a nossa atenção no ponto onde está o verdadeiro problema: não no estresse
contido nas experiências, mas na nossa ignorância em pensar que nós temos que
nos apegarmos a elas. E é uma coisa boa também, pois é aí que está o problema.
Enquanto existirem montanhas, não há muito que possamos fazer sobre o seu peso
inerente, mas nós podemos aprender a quebrar o nosso hábito de levantá-las e
carregá-las por aí. Nós podemos aprender a parar de nos apegarmos. Isso porá um
fim aos nossos sofrimentos.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Para compreender como soltar das coisas de forma eficaz, é de grande
ajuda dar uma olhada na palavra apego em Pali – <i>upadana</i> – pois ela
também tem um segundo sentido: o ato de alimentar-se, como quando a planta se
alimenta do solo ou o fogo do seu combustível.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Esse segundo significado para <i>upadana</i> se aplica à mente também.
Quando a mente se apega a um objeto, ela está se alimentando desse objeto. Está
tentando ganhar alimento dos prazeres sensuais, possessões, relacionamentos,
reconhecimento, status, o que quer que seja, para compensar o corrosivo sentido
de vazio que ela sente por dentro. Infelizmente, esse alimento mental é
temporário na melhor das hipóteses, então ficamos famintos por mais. No
entanto, não importa o quanto a mente tente possuir e controlar suas fontes de
alimento para garantir um fornecimento constante, elas inevitavelmente se
acabam.<span style="mso-spacerun: yes">  </span>A mente fica então
sobrecarregada com a busca de novos lugares para se alimentar.<o:p></o:p></span></p>

<p class=Normal> <span lang=PT-BR style='mso-ansi-language:
PT-BR'>Assim o problema do estresse se resume aos hábitos alimentares da mente.
Se a mente não tivesse que se alimentar, ela não sofreria. Ao mesmo tempo, ela
não criaria mais dificuldades – através da possessão e controle – para as
pessoas e coisas que ela consome <span style="mso-spacerun: yes"> </span>como
alimento. Se quisermos terminar com o sofrimento para nós mesmos e ao mesmo
tempo aliviar as dificuldades dos outros, nós deveremos então fortalecer a
mente até o ponto em que ela não precise se alimentar, e então aguçar o seu
discernimento para que<span style="mso-spacerun: yes">  </span>ela não <i>queira</i>
se alimentar. Quando ela nem precisar e nem quiser se alimentar mais, ela se
soltará sem termos que dizer que assim o faça. <o:p></o:p></span></p>


<p class=Normal> A prática para dar fim a dukkha
seria rápida e fácil se nós simplesmente pudéssemos chegar direto à sabedoria
que dá um fim ao apego. Assim, poderíamos então progredir pelo resto das
nossas<span style="mso-spacerun: yes">  </span>vidas. A analogia da
alimentação, no entanto, ajuda a explicar porque, simplesmente, ver as
desvantagens do apego não é suficiente para fazer com que nos soltemos. Se não
formos fortes o suficiente para ficar sem o alimento, a mente continuará
encontrando novas maneiras para se alimentar e se apegar. Portanto, nós primeiro
temos que aprender hábitos alimentares saudáveis que fortaleçam a mente. Só
então, ela estará numa posição em que não precise se alimentar.</span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Como a mente se alimenta e se apega? O Cânone em Pali lista quatro
formas:<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> 1. apego
à cobiça sensual pela visão, sons, aromas, sabores e tangíveis;<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> 2. apego
às idéias sobre o mundo e às narrativas de nossas vidas;<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> 3. apego
a preceitos e rituais – isto é, maneiras fixas de fazer as coisas; e <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> 4. apego à idéia da existência de um eu – isto
é, idéias sobre se temos ou não uma verdadeira identidade, ou o que seria essa
identidade.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>É raro o momento em que a mente comum não esteja se apegando a pelo
menos uma dessas formas. Mesmo quando abandonamos uma forma de apego,
normalmente, é para favorecer uma outra. Por exemplo, podemos abandonar uma
opinião puritana porque interfere com o prazer sensual; ou um prazer sensual
porque conflita com uma opinião sobre o que deveríamos fazer para nos mantermos
saudáveis e em forma. Nossa opinião sobre quem somos pode variar dependendo de
qual das nossas várias idéias de “eu” esteja mais sensibilizada, expandindo
para um sentido de unidade cósmica quando nos sentimos confinados pelo nosso
pequeno complexo de corpo e mente e contraindo para dentro de uma casca quando
nos sentimos feridos devido à identificação com um cosmo tão cheio de crueldade
e desperdício. Quando a insignificância de nosso eu finito se torna opressiva
novamente, nós podemos saltar para uma idéia de que nós não temos nenhum eu,
mas então <i>isso</i> se torna opressivo.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Assim nossas mentes saltam de apego em apego como um pássaro preso numa
gaiola. Quando percebemos que estamos cativos, nós naturalmente buscamos uma
saída, mas para qualquer lugar que nos voltemos, parece ser um outro lado da
gaiola. Podemos começar a nos indagar se existe uma saída, ou se a conversa de
completa liberdade não passa de um antigo arquétipo ideal que não tem nada a
ver com a realidade humana. Mas o Buda foi um grande estrategista: ele percebeu
que uma das paredes da gaiola é na verdade uma porta e que se nós a tocarmos de
maneira hábil, ela se abrirá completamente.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Em outras palavras, ele descobriu que a maneira de superar o apego é
transformar as nossas quatro formas de apego em um caminho para o seu próprio
abandono. Precisaremos de uma certa dose de prazer sensual – no que se refere a
um alimento adequado, roupas e moradia -<span style="mso-spacerun: yes"> 
</span>para encontrar a força para superar a cobiça sensual. Necessitaremos de
entendimento correto – que vê todas as coisas, inclusive as idéias, sob a
perspectiva das Quatro Nobres Verdades – para minar nosso apego às idéias. E
todos precisaremos das regras dos cinco preceitos éticos e da prática da
meditação, para colocar a mente em uma posição sólida, onde ela possa derrubar
seu apego aos preceitos e rituais. Sustentando tudo isso, precisaremos de um
saudável sentido de amor próprio, auto-responsabilidade e auto-disciplina para
dominar as práticas que levam ao insight que corta todo apego à idéia da
existência de um eu.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Assim, começamos o caminho para o fim do sofrimento, sem tentar derrubar
nossos apegos imediatamente, mas aprendendo a nos apegar mais estrategicamente.
Em relação à analogia da alimentação, nós não tentamos matar a mente de fome. Simplesmente
mudamos a sua dieta, fazendo com que ela substitua a comida sem qualidade pela
comida saudável, desenvolvendo qualidades interiores que a tornarão tão forte
que ela não precisará se alimentar nunca mais.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>O Cânone em Pali lista essas qualidades como sendo cinco: <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> 1. convicção
no princípio de kamma – que nossa felicidade depende das nossas próprias ações<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> 2. energia
para abandonar qualidades inábeis e desenvolver qualidades hábeis no seu lugar<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> 3. atenção
plena<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> 4. concentração
<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> 5. sabedoria<o:p></o:p></span></p>

<p class=Normal >De todas essas, a concentração – no
nível de <i>jhana</i>, ou absorção intensa, é a força que a tradição Budista
mais freqüentemente compara com comida boa e saudável. Um discurso do <i>Anguttara
Nikaya</i> (AN VII.63) compara os quatro níveis de jhana às provisões usadas
para abastecer uma fortaleza fronteiriça. Ajaan Lee, um dos mestres da tradição
de florestas da Tailândia, compara<span style="mso-spacerun: yes">  </span>os
jhanas com as provisões necessárias para uma viagem por uma floresta isolada e
desabitada. Ou como o <i>Dhammapada</i> (200) diz sobre o êxtase do jhana:</span></p>

<p class=Normal align=center style='text-align:center'><i><span
lang=PT-BR>Quão felizes vivemos nós,<br>

que não temos nada.<br>

Que nos alimentamos do êxtase<br>

como os devas Radiantes</span></i><span lang=PT-BR>.</span></p>

<p class=Normal>Quanto à sabedoria, quando a mente está
fortalecida com a boa comida da concentração, ela pode começar a considerar as
desvantagens de ter que se alimentar. Essa é a parte dos ensinamentos do Buda
que – para muitos de nós – vai contra a nossa inclinação natural, porque
alimentação, em todos os sentidos da palavra, é a maneira mais importante<span
style="mso-spacerun: yes">  </span>de nos relacionarmos e obtermos satisfação
do mundo que nos rodeia.<span style="mso-spacerun: yes">  </span>O nosso
sentido mais acalentado de interconexão com o mundo – que algumas pessoas
chamam de relacionar-se – é, no seu nível mais básico, comer uns aos outros.
Nós nos alimentamos dos outros e eles se alimentam de nós. Algumas vezes nossos
relacionamentos são mutuamente nutritivos, algumas vezes não; mas de qualquer
forma é muito difícil imaginar qualquer relacionamento duradouro, onde algum
tipo de alimentação física ou mental não esteja em jogo. Ao mesmo tempo, a
alimentação é a atividade na qual experimentamos o sentido mais íntimo de nós
mesmos. Nós nos definimos através de prazeres, pessoas, idéias e atividades
para as quais sempre retornamos para nos alimentarmos.<span
style="mso-spacerun: yes">  </span></span></p>

<p class=Normal> Assim é difícil que imaginemos no
mundo, qualquer possibilidade de prazer onde não estivéssemos comendo uns aos
outros. Nossa resistência generalizada à idéia de não mais nos alimentarmos –
um dos mais incomuns e radicais ensinamentos do Buda – vem em grande parte de
uma falha de imaginação. Nós quase não podemos conceber o que ele está tentando
nos dizer. Assim, ele tem que prescrever um remédio forte para estimular nossas
mentes a enxergar<span style="mso-spacerun: yes">  </span>novas perspectivas.</span></p>

<p class=Normal> Aqui é onde os
seus ensinamentos sobre dukkha, ou estresse, entram em ação. Quando a mente é
forte e bem alimentada, ela pode começar a olhar objetivamente para o estresse
envolvido em ter que se alimentar. Os ensinamentos sobre dukkha, como uma das
três características comuns, focam nas desvantagens daquilo que a mente toma
como alimento. Algumas vezes, ela se agarra a um total sofrimento. Ela se apega
ao corpo mesmo quando alquebrado pela dor. Ela se apega às suas preferências e
relacionamentos mesmo quando eles trazem angústia, dor e desespero. Algumas
vezes, a mente se agarra a prazeres e alegrias, mas prazeres e alegrias<span
style="mso-spacerun: yes">  </span>se tornam cheios de estresse quando eles se
deterioram e mudam. Tudo aquilo a que a mente se agarra, em qualquer situação,
é condicionado devido a sua própria natureza, e existe sempre, pelo menos, um
nível sutil de estresse inerente para manter esse condicionamento em constante
movimento. Isso se aplica não somente às condições externas mais grosseiras,
mas mesmo aos níveis mais sutis de concentração na mente.</span></p>

<p class=Normal>Quando vemos o estresse como uma
característica comum a todas as coisas às quais nos agarramos, isso ajuda a
dissipar a sua sedução. Os prazeres começam a dar a impressão de vazios e
falsos. Mesmo nossos sofrimentos – os quais quase sempre podemos encher de<span
style="mso-spacerun: yes">  </span>glamour com um orgulho perverso – começam a
parecer banais quando reduzidos à sua característica de estresse. Isso ajuda a
colocá-los no seu tamanho exato.</span></p>

<p class=Normal>É claro, algumas pessoas se opõem à
idéia de considerar dukkha como inerente<span style="mso-spacerun: yes"> 
</span>à comida da mente, fundamentados em que essa consideração não faz
justiça às muitas alegrias e satisfações da vida. O Buda, entretanto, nunca
negou a existência do prazer.<span style="mso-spacerun: yes">  </span>Ele
simplesmente mostrou que se você focar na sedução da sua comida, você nunca
será capaz de superar seus vícios alimentares. Seria como pedir a um alcoólatra
que ponderasse sobre as sutilezas aromáticas do uísque e do vinho. </span></p>


<p class=Normal>Dukkha é inerente não somente às coisas
com as quais nos alimentamos, mas também ao ato de se alimentar em si. E este é
o ponto<span style="mso-spacerun: yes">  </span>central dos ensinamentos do
Buda sobre dukkha como uma nobre verdade. Se temos que nos alimentar, nós somos
escravos do nosso apetite. E podemos confiar em nós mesmos, quanto a
comportarmo-nos de maneira honrada, quando as exigências desses desejos que
escravizam não forem atendidas?<span style="mso-spacerun: yes">  </span>Comer
uns aos outros nem sempre é uma idéia agradável. Ao mesmo tempo, enquanto
precisarmos nos alimentar seremos presas de todas as incertezas em relação às
nossas fontes de alimento, à mercê de qualquer pessoa ou força com poder sobre
elas. Se não podemos ficar sem ele, nós estamos acorrentados a ele.<span
style="mso-spacerun: yes">  </span>A mente não é livre para ir aonde não há
comida.<span style="mso-spacerun: yes">  </span>E, como o Buda garante, aqueles
são exatamente os lugares –<span style="mso-spacerun: yes">  </span>muito além
do nosso horizonte mental comum – onde se encontra a maior felicidade.</span></p>

<p class=Normal>O propósito dessas duas considerações –
sobre o estresse inerente a ambos, o alimento da mente e como ela se alimenta –
é de nos sensibilizar para limitações que de qualquer forma aceitamos, algumas
vezes com tranqüilidade, mas sempre cegamente e sem pensar. Uma vez que
finalmente compreendamos que ela não vale o preço que nos impõe, perdemos toda
a paixão pelo nosso desejo de alimentação. E, ao contrário do corpo, a mente
pode alcançar um nível de força em que não mais necessitará apegar-se ou
alimentar-se, nem mesmo do caminho da prática. Quando ela se torna forte o
suficiente na convicção, energia, atenção plena, concentração e sabedoria, ela
poderá se abrir para uma dimensão – imortal – onde não existe nem<span
style="mso-spacerun: yes">  </span>alimentação e nem o ser alimentado. Isto põe
um fim ao “processo alimentar” e não existe mais o sofrimento no que se refere
à comida.<span style="mso-spacerun: yes">  </span>Em outras palavras, uma vez
que penetremos completamente o imortal, dukkha como uma das três características
comuns não será mais um problema; dukkha como uma nobre verdade não mais
existirá.</span></p>

<p class=Normal>É quando você descobre algo inesperado:
as montanhas que você vem tentando levantar são todas um subproduto da sua
alimentação. Quando você parar de se alimentar, não haverá a formação de novas
montanhas. Embora ainda possam existir algumas montanhas restantes de carmas
passados ao seu redor, elas finalmente desaparecerão, mas nenhuma montanha nova
aparecerá no lugar delas.<span style="mso-spacerun: yes">  </span>Nesse meio
tempo, o peso delas não será mais um problema. Uma vez que você, finalmente,
pare de tentar levantá-las, não haverá nada que o detenha.<span
style="mso-spacerun: yes">   </span></span></p>


