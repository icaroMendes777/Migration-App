<p class=Normal>Para um monge (um monge ou monja) a comida deve ser considerada como se fosse um remédio e não como um prazer. No espírito do Caminho do Meio, apenas a quantidade suficiente deve ser ingerida para manter o corpo saudável. No <a href="../sutta/MN70.php">Kitagiri Sutta</a> é encontrada a seguinte passagem:</a>

<p class=Normal style='margin-left:.75in'> "Em certa ocasião, o Abençoado estava perambulando por Kasi junto com uma grande Sangha de bhikkhus. Lá ele se dirigiu aos monges desta forma:</p>

<p class=Normal style='margin-left:.75in'> “Bhikkhus, eu me abstenho de comer à noite. Fazendo isso, estou livre de enfermidades e aflições e desfruto de boa saúde, energia e bem-estar. Venham, bhikkhus, abstenham-se de comer à noite. Fazendo isso, vocês também estarão livres de enfermidades e aflições e desfrutarão de boa saúde, energia e bem-estar.”</p>

<p class=Normal>    Consequentemente, comer apenas no período do amanhecer até o meio-dia tornou-se a tradição para os monges budistas, mas não sem alguma resistência no início. No <a href="../sutta/MN66.php">Latukikopama Sutta</a> o Venerável Udayin diz o seguinte para o Buda :</p>

<p class=Normal style='margin-left:.75in'>  "Venerável senhor, no passado costumávamos comer à noite, pela manhã e durante o dia, fora do horário apropriado. Então, houve uma ocasião em que o Abençoado se dirigiu aos bhikkhus da seguinte forma: ‘Bhikkhus por favor abandonem essa refeição durante o dia, fora do horário apropriado.’ Venerável senhor, eu fiquei preocupado e triste, pensando: ‘Chefes de Família dedicados nos dão bons alimentos de vários tipos durante o dia, fora do horário apropriado, no entanto, o Abençoado nos diz para abandonar isso, o Sublime nos diz para renunciar a isso.’"</p>

 <p class=Normal style='margin-left:.75in'> “Então apenas comíamos à noite e pela manhã. Então houve uma ocasião em que o Abençoado se dirigiu aos bhikkhus da seguinte forma: ‘Bhikkhus por favor abandonem essa refeição noturna, que está fora do horário apropriado.’ Venerável senhor, eu fiquei preocupado e triste, pensando: ‘O Abençoado nos diz para abandonar a mais suntuosa dentre as nossas duas refeições, o Sublime nos diz para renunciá-la.’"</p>

<p class=Normal> Mas o Venerável Udayin, que por sinal era conhecido por ser gordo, logo superou a sua decepção e percebeu os benefícios da restrição no horário da comida. Além disso, ele reconheceu que como monges eles obtinham o seu alimento, principalmente na ronda de esmola de alimentos, e sair para esmolar à noite tinha algumas desvantagens. O mesmo sutta continua:</p>

<p class=Normal style='margin-left:.75in'>  “Já aconteceu, venerável senhor, que bhikkhus perambulando por alimentos no negrume da noite acabaram caindo numa fossa, num esgoto, se meteram num arbusto espinhoso e tropeçaram numa vaca dormente; eles encontraram criminosos que já haviam cometido crime e aqueles que estavam planejando cometê-lo, e eles foram seduzidos por mulheres. Certa vez, venerável senhor, eu fui perambular por alimentos no negrume da noite. Uma mulher lavando um pote me viu através do clarão de um relâmpago e gritou horrorizada: ‘Piedade, o diabo veio me buscar!’ Eu lhe disse: ‘Irmã, eu não sou o diabo, eu sou um bhikkhu perambulando por alimentos’ – ‘Então é um bhikkhu cuja mãe e pai faleceram! Melhor, bhikkhu, que você tenha a sua barriga cortada por uma faca afiada de açougueiro do que essa ronda por esmolas no negrume da noite em benefício da sua barriga!’"</p>

<p class=Normal> Um monge ou monja que come comida fora do horário apropriado, que é  do amanhecer até o meio-dia, comete uma ofensa chamada Pacittiya. Para se redimir da ofensa eles devem ir até  um companheiro monge ou monja e confessar a ofensa. Este simples reconhecimento é tudo o que é necessário pois isso serve para aumentar o senso de dever e de responsabilidade, e por isso é um forte incentivo para que alguém seja mais meticuloso no futuro. Assim, essas regras do Vinaya não se baseiam na punição, mas sim encorajam mais atenção plena e contenção de modo que o mesmo erro não se repita.</p>

<p class=Normal>    Embora alimentos sólidos não sejam permitidos fora do período da manhã, outras substâncias foram autorizadas no período da tarde ou à noite, especialmente quando há uma necessidade como uma doença. Frutas coadas e sucos vegetais são admissíveis no período da tarde, especialmente para monges e monjas sedentos. Também os cinco "medicamentos" tradicionais indianos: ghee, óleo, manteiga/queijo, mel e açúcar foram autorizados no período da tarde como um "tônico" para ser usado em situações de trabalho pesado, devido ao frio, ou quando há muito pouca comida esmolada. Caldos claros de carne ou feijões são permitidos no período da tarde para monges ou monjas muito enfermos. Bebidas como chá, infusões de ervas, gengibre, cacau e café também são permitidas na parte da tarde, tanto quanto um monge ou monja sintam necessidade. No entanto o leite é o objeto de alguma controvérsia. Alguns monges dizem que é permitido no período da tarde, alguns dizem que não é; a nossa tradição diz que não é.</p>

<p class=Normal>    Qualquer coisa que seja permitida e aquilo que não é permitido, e quando não é permitido, um bom monge ou monja sempre se lembra do PORQUÊ comer. O  enunciado do Buda que segue abaixo deve sempre ser lembrado quando algum alimento for ingerido, sendo ouvido no nosso monastério recitado por um dos monges antes de cada refeição:</p>

<p class=Normal style='margin-left:.75in'>"Refletindo de maneira sábia, um bhikkhu não toma o alimento como forma de diversão ou para embriaguez, tampouco com o objetivo de embelezamento e para ser mais atraente; mas somente com o propósito de manter a resistência e continuidade deste corpo, como forma de dar um fim ao desconforto e para auxiliar a vida santa, considerando: ‘Dessa forma darei um fim às antigas sensações (de fome) sem despertar novas sensações (de comida em excesso) e serei saudável e sem culpa e viverei em comodidade.’" (<a href="../sutta/ANIII.16.php">Apannaka Sutta</a>)</p>

