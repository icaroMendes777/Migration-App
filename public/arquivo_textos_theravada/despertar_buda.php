<p class=Normal>Os dois
aspectos cruciais do Despertar do Buda são <i>o</i> <i style='mso-bidi-font-style:
normal'>que </i>e <i>o</i><i style='mso-bidi-font-style:normal'> como</i>: para
o que ele despertou e como ele fez isso. O despertar dele é especial porque
esses dois aspectos se unem. Ele despertou para o fato de que existe uma
felicidade imortal e de que ela pode ser alcançada através do esforço humano.
No final das contas, o esforço humano envolvido nesse processo<span
style="mso-spacerun: yes">  </span>foca na questão da compreensão da natureza
do próprio esforço humano – em relação a kamma habilidoso e origem dependente –
quais são os seus poderes e limitações e que tipo de esforço correto ( isto é,
o Nobre Caminho) poderá levar a pessoa para além dessas limitações e conduzí-la
ao limiar do Imortal.<o:p></o:p></span></p>


<p class=Normal>O Buda
descreve a experiência do Despertar num dos seus discursos, primeiro surge a
compreensão da regularidade do Dhamma – que nesse contexto quer dizer a origem
dependente – depois existe a compreensão de Nibbana. Em outros trechos ele
descreve os três estágios que o conduziram ao insight da origem dependente:
compreensão das vidas passadas, compreensão da morte e renascimento de todos os
seres vivos e por fim o insight das quatro Nobres Verdades. As duas primeiras
formas de conhecimento não foram novidades reveladas pelo Buda. Elas haviam
sido reportadas por outros videntes ao longo da história, embora o insight do
Buda do segundo conhecimento tivesse um pormenor importante: ele viu que os
seres renascem de acordo com a qualidade ética dos seus pensamentos, palavras e
atos, e de que essa qualidade é em essência um fator da mente. A qualidade das
idéias e intenções da pessoa determinam o resultado das ações que ela irá
experimentar. <o:p></o:p></span></p>

<p class=Normal>Esse
insight teve um duplo impacto na sua mente. Por um lado, fez com que ele
compreendesse a futilidade do ciclo de renascimentos – mesmo os melhores
esforços, que visam obter prazer e satisfação dentro do ciclo, podem ter
efeitos apenas temporários. Por outro lado, a compreensão da importância da
mente na determinação do ciclo levou-o a focar diretamente na sua própria mente
no presente, para ver como os processos mentais que mantêm o ciclo em
funcionamento poderiam ser desfeitos. Assim foi como ele obteve o insight das
quatro nobres verdades e da origem dependente – vendo como os agregados que
compunham a “pessoa” dele eram também os fatores que impeliam o ciclo de
experiências e do mundo de forma geral e como esse processo todo poderia ser
cessado. Com a sua cessação, resta a experiência do incondicionado, que ele
também denominou nibbana (desatamento), a consciência sem fronteiras ou
características, o Imortal.<o:p></o:p></span></p>

<p class=Normal>Quando nos
referimos à questão sobre como outras experiências “iluminadas” registradas na
história mundial se relacionam com o Buda, devemos ter em mente aquilo que o
próprio Buda disse: primeiro existe a compreensão da origem dependente, depois
existe a compreensão de nibbana. Sem o primeiro – que inclui não somente a
compreensão de kamma, mas também como kamma, em si, conduz à própria
compreensão – qualquer realização, não importa quão pacífica ou ilimitada, que
não resulte desse tipo de compreensão não pode ser considerada como Despertar
no sentido Budista. O verdadeiro Despertar necessariamente envolve ambos, a
ética e o insight da causalidade.<o:p></o:p></span></p>

<p class=Normal>Quanto
àquilo que o Despertar do Buda significa para nós hoje, quatro pontos se
destacam.<o:p></o:p></span></p>

<p class=Normal>1) O papel
que kamma desempenha no Despertar é o de conferir poder. Significa que aquilo
que cada um de nós faz, diz e pensa <i>tem </i>importância – isso, em oposição
à sensação de futilidade que pode advir ao ler, digamos, sobre a história do
mundo, geologia ou astronomia e compreender a natureza fugaz de toda a
iniciativa humana. O Despertar nos permite ver que as escolhas que fazemos em
cada momento das nossas vidas têm consequências. Não somos estrangeiros em um
país estranho. Nós formamos e continuamos a formar o mundo que experimentamos.
O fato de sermos investidos de poder também significa que somos responsáveis
pelas nossas experiências. Isso nos ajuda a enfrentar os eventos da vida com
maior equanimidade, pois sabemos que influenciamos a criação deles e ainda, ao
mesmo tempo, podemos evitar os sentimentos debilitantes de culpa, porque com
cada nova escolha podemos sempre começar de novo.<o:p></o:p></span></p>

<p class=Normal>2) O
Despertar também nos diz que o bem e o mal não são meras convenções sociais,
mas estão inseridos na mecânica de como o mundo é construído. Somos livres para
esboçar as nossas vidas, mas não somos livres para mudar as regras subjacentes
que determinam o que são ações benéficas e prejudiciais e como o processo de
kamma opera. Dessa forma, o relativismo cultural – muito embora isso possa ter
pavimentado o caminho para que muitos de nós deixássemos as nossas antigas
inclinações religiosas e aderisse à congregação Budista – não tem lugar, uma
vez que estejamos inseridos nessa congregação. Existem certas formas de agir
que são inábeis e prejudiciais por sua natureza, e somos tolos se insistirmos
no nosso direito de nos comportarmos de acordo com elas.<o:p></o:p></span></p>

<p class=Normal>3) Em certo
ponto ao descrever o seu Despertar o Buda disse, “A ignorância foi destruída;
surgiu o conhecimento; a escuridão foi destruída; surgiu a luz – como ocorre
com aquele que é diligente, ardente e decidido.” Em outras palavras ele obteve
o conhecimento libertador através de qualidades que todos podemos desenvolver:
diligência, ardência, decisão. Se estivermos dispostos a enfrentar as
implicações deste fato, compreenderemos que o Despertar do Buda é um desafio a
todo o nosso sistema de valores. O fato de que o Incondicionado pode ser
alcançado força-nos a reavaliar quaisquer outros objetivos que tenhamos
definido para nós mesmos, quaisquer que sejam os mundos que queiramos criar nas
nossas vidas. Num nível óbvio, ele aponta para a pobreza espiritual de uma vida
dedicada à riqueza, status ou de busca pela sensualidade; mas também nos força
a olhar com franqueza para outros objetivos “benéficos” que a nossa cultura e
as suas sub-culturas tendem a exaltar, tais como a aceitação social,
relacionamentos significativos, gerenciamento do planeta, etc. Esses, também,
conduzem inevitavelmente ao sofrimento. A interdependência de todas as coisas
não pode ser, para uma mente realmente sensível, uma fonte de segurança e
conforto. Se o Incondicionado pode ser alcançado, e é a única fonte de
felicidade confiável disponível, faz todo sentido investirmos os nossos
esforços e todos os recursos mentais e espirituais de que dispomos na sua
direção.<o:p></o:p></span></p>

<p class=Normal>4) Mesmo
para aqueles que não estão preparados para fazer esse tipo de investimento, o
Despertar nos assegura que a felicidade advém do desenvolvimento de qualidades
dentro de nós mesmos, das quais podemos nos orgulhar, tais como a bondade,
sensibilidade, equanimidade, atenção plena, convicção, determinação e
sabedoria. Mais uma vez, essa é uma mensagem bem diferente daquela que colhemos
do mundo, que nos diz que para obter a felicidade precisamos desenvolver
qualidades das quais não podemos ter nenhum orgulho genuíno: agressividade,
engrandecimento pessoal, desonestidade, etc. Só isso pode dar uma orientação
completamente nova para as nossas vidas e idéias sobre onde vale a pena
investir nosso tempo e esforço.<o:p></o:p></span></p>

<p class=Normal>O relato do
Despertar do Buda define o padrão para julgar a cultura na qual fomos educados
e não o contrário. Essa não é uma questão de escolher a cultura Asiática em
lugar da Ocidental. O Despertar do Buda desafiou muitas das pressuposições da
cultura Hindu da época; e mesmo nos assim chamados países Budistas, a
verdadeira prática Budista é sempre parte da contracultura. É uma questão de
avaliar as nossas preocupações usuais – condicionadas pelo tempo, espaço e as
limitações de envelhecimento, enfermidade e morte – contra a possibilidade de
uma felicidade imortal, sem fronteiras, sem limites. Todas as culturas estão
apegadas ao lado limitado, condicionado das coisas, enquanto que o Despertar do
Buda aponta para mais além de todas as culturas. Ele oferece o desafio do
Imortal que os seus contemporâneos descobriram ser liberador e que nós, se
estivermos dispostos a aceitar o desafio, poderemos fazer a mesma descoberta
por nós mesmos.<o:p></o:p></span></p>

