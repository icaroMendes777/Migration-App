<p class=Normal><i>Um Budismo que faça uma afirmação positiva<span style="mso-spacerun:
yes">  </span>em relação à vida e que nos ensine a encontrar a felicidade
através da abertura para a riqueza contida no nosso dia a dia. </span></i><span
lang=PT-BR style='font-size:12.0pt;mso-bidi-font-size:10.0pt'><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso é o que queremos - ou
assim nos dizem as pessoas que nos tentam vender o Budismo. Mas é isso que
precisamos? E será Budismo? <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Reflita por um momento
acerca da história do jovem Príncipe Siddharta e o seu primeiro encontro com a
velhice, enfermidade, morte e com um contemplativo. É um dos capítulos mais
acessíveis da tradição Budista, em grande parte pela qualidade direta, sincera,
das emoções do jovem príncipe. Ele viu a velhice, enfermidade e morte como o
terror absoluto e colocou todas as suas esperanças na vida contemplativa na
floresta como única escapatória. Da forma como Asvaghosa, o grande poeta
Budista, descreve a estória, ao jovem não lhe faltaram amigos e familiares que
tentaram convencê-lo a abandonar essas percepções e, Asvaghosa foi suficientemente
sábio em mostrar esse aconselhamento sob uma ótica de afirmação da vida de
forma muito atraente. Apesar disso, o príncipe se deu conta de que se ele
aceitasse o conselho, ele estaria traindo seu coração. Somente sendo
absolutamente sincero com as suas verdadeiras emoções pôde ele seguir o caminho
que o afastou dos valores tradicionais da sua sociedade em direção ao Despertar
insuperável para o Imortal. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Dificilmente essa é uma
estória de afirmação da vida, no sentido comum da palavra. Porém, ela afirma
algo mais importante que a vida: a verdade do coração quando ele aspira uma
felicidade absolutamente pura. O poder dessa aspiração depende de duas emoções
denominadas em Pali <i>samvega </i>e <i>pasaada. </i>Poucos de nós as ouvimos
antes, porém elas são as emoções mais básicas na tradição Budista. Não somente
elas inspiraram o jovem príncipe na sua busca pela iluminação, mas mesmo depois
que ele se tornou o Buda ele aconselhou aos seus seguidores a cultivá-las
diariamente. Na verdade, a maneira como ele lidou com essas emoções é tão
distinta que pode ser uma das mais importantes contribuições que os seus
ensinamentos têm a oferecer para a cultura contemporânea. <o:p></o:p></span></p>

<p class=Normal><i><span lang=PT-BR style='mso-ansi-language:PT-BR'>Samvega</span></i><span
lang=PT-BR style='mso-ansi-language:PT-BR'> foi o que<span style="mso-spacerun:
yes">  </span>o jovem Príncipe Siddhartha sentiu na sua primeira exposição à
velhice, enfermidade e morte. É uma palavra difícil de traduzir porque abrange
um espectro tão complexo - pelo menos três conjuntos de sentimentos de uma vez:
o sentimento opressivo de choque, consternação e alienação, que surge ao se dar
conta da futilidade e falta de sentido da vida da forma como ela é normalmente
vivida; o sentimento de culpa por nossa própria complacência e tolice em
permitir-nos viver de maneira tão cega; e um senso de urgência em tentar
encontrar uma saída desse ciclo sem sentido. Todos experimentamos esse<span
style="mso-spacerun: yes">  </span>conjunto de sentimentos uma vez ou outra
durante nosso processo de crescimento. Porém, não existe uma única palavra em
Português que possa adequadamente abranger todos os três. Seria útil ter um
termo assim e talvez essa seja uma razão suficiente para adotar a palavra <i>samvega
</i>como parte do nosso vocabulário. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Mas além de nos
proporcionar um termo útil, o Budismo também oferece uma estratégia eficaz de
como lidar com esses sentimentos - sentimentos que a nossa cultura vê como uma
ameaça e que não sabe muito bem como lidar. A nossa cultura, é claro, não é a
única ameaçada pelo sentimento de <i>samvega</i>. Na estória de Siddharta, a
reação do pai em relação à descoberta feita pelo jovem príncipe ilustra a
maneira como a maioria das culturas tenta lidar com esses sentimentos. Ele
tentou convencer o príncipe de que os seus objetivos de felicidade eram
inatingíveis, ao mesmo tempo que tentava distrai-lo com relacionamentos e todo
tipo de prazer sensual imaginável. Para colocar de maneira simplificada, a
estratégia era fazer com que o príncipe diminuísse os seus objetivos e
encontrasse satisfação em uma felicidade que estava longe de ser absoluta e não
particularmente pura.<span style="mso-spacerun: yes">  </span><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Se o jovem príncipe vivesse
na nossa sociedade contemporânea, o pai teria outras ferramentas para lidar com
a insatisfação do príncipe, porém a estratégia básica seria essencialmente a
mesma. Podemos facilmente imaginar ele levando o príncipe a um conselheiro
religioso que lhe ensinaria a acreditar que a criação de Deus é basicamente boa
e a não focar nos aspectos da vida que possam colocar em dúvida essa crença. Ou
ele o levaria a um psicanalista que trataria os sentimentos de <i>samvega</i>
como uma incapacidade para aceitar a realidade. Se a terapia não surtisse
resultados, o terapeuta provavelmente prescreveria drogas antidepressivas para
anestesiar os sentimentos do jovem de forma a que ele se tornasse novamente um
membro da sociedade produtivo e bem ajustado. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Se o pai estivesse
realmente afinado com as tendências mais recentes, ele poderia encontrar um
professor do Dhamma que aconselharia o príncipe a encontrar a felicidade nos
pequenos prazeres da vida - uma xícara de café, um passeio no bosque, ativismo
social, aliviar a dor de outras pessoas. Não importa que essas formas de
felicidade sejam encurtadas pelo envelhecimento, enfermidade e morte lhe
diriam. O momento presente é tudo que temos, assim devemos tentar desfrutar a
oportunidade acre-doce de saborear mas não de agarrar-nos às pequenas alegrias
à medida que elas passam. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>É improvável que o príncipe
determinado que conhecemos da estória aceitasse qualquer um desses conselhos
bem intencionados. Ele os veria como propaganda de uma vida de silenciosa
desesperação, convidando-o a trair os desígnios do seu coração. Porém se ele
não encontrasse consolo dessas fontes, aonde em nossa sociedade ele poderia ir?
Ao contrário da Índia do seu tempo, nós não temos alternativas estabelecidas,
socialmente aceitáveis, para viver como membros economicamente produtivos da
sociedade. Mesmo as ordens religiosas contemplativas são valorizadas pela sua
habilidade em prover os mercados com pão, mel e vinho. Dessa forma o príncipe
provavelmente não encontraria outra alternativa que juntar-se aos excluídos, os
radicais e revolucionários, um sobrevivente vivendo no limite da subsistência,
consignado à marginalidade social. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ele descobriria muitas
mentes refinadas e espíritos sensíveis nesses grupos, porém nenhum conjunto de
sabedoria alternativa profunda e comprovada da qual pudesse extrair algo.
Alguém poderia lhe dar um livro de Thoreau ou Muir, porém os seus escritos não
lhe ofereceriam uma análise satisfatória do envelhecimento, enfermidade e
morte, e nenhuma recomendação de como superá-los. E devido a que não existe
nenhum tipo de apoio para as pessoas que vivem na marginalidade social, ele se
veria forçado a colocar um esforço desproporcional em questões básicas de
sobrevivência, com pouco tempo ou energia disponível para encontrar por si
mesmo a solução para o problema de <i>samvega</i>. Ele terminaria por
desaparecer, abortando a sua busca pela natureza Budica - talvez em algum ponto
remoto do país - sem deixar vestígio. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Felizmente para nós no
entanto, o príncipe nasceu em uma sociedade que <i>provia </i>apoio e respeito
aos que abandonavam a sociedade. Isso foi o que<span style="mso-spacerun:
yes">  </span>lhe deu a oportunidade de encontrar uma solução para o problema
de <i>samvega</i> que fizesse justiça às verdades do seu coração. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O primeiro passo dessa
solução é simbolizado na estória de Siddhartha pela reação do príncipe em
relação à quarta pessoa que ele viu nas suas saídas do palácio: o
contemplativo. A emoção que ele sentiu nesse momento é denominada <i>pasaada, </i>outro
conjunto complexo de sentimentos normalmente traduzido como &quot;claridade e
serena confiança.&quot; É o que<span style="mso-spacerun: yes">  </span>evita
que <i>samvega</i> se transforme em desespero. No caso do príncipe, ele obteve
uma clara percepção da sua difícil situação e da maneira como escapar dela,
conduzindo a algo que está além do envelhecimento, enfermidade e morte, e ao
mesmo tempo sentindo-se confiante de que o caminho iria dar resultado. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os ensinamentos Budistas
mais antigos admitem abertamente que o poblema é que o ciclo de nascimento,
envelhecimento e morte <i>n­ão tem</i> sentido. Eles não tentam negar esse fato
e dessa forma não pedem que sejamos desonestos conosco mesmos ou que fechemos
nossos olhos para a realidade. Conforme formulado por um mestre, o
reconhecimento pelo Budismo da realidade do sofrimento - tão importante que o
sofrimento é honrado como a primeira nobre verdade - é uma dádiva, no sentido
de que confirma a maneira mais direta e sensível como experimentamos as coisas,
uma experiência que muitas outras tradições tentam negar. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A partir daí, os
ensinamentos nos pedem que nos tornemos ainda mais sensíveis, até o ponto em
que possamos ver que a verdadeira causa do sofrimento não está <i>no exterior </i>-
na sociedade ou em algum outro ser - mas <i>aqui dentro, </i>no desejo presente
em cada mente. Eles então confirmam que existe um fim para o sofrimento, a
libertação do ciclo. E eles então mostram o caminho para essa libertação,
através do desenvolvimento das nobres qualidades que estão latentes na mente,
até o ponto em que o desejo é colocado de lado e ocorre a abertura para o
Imortal. Dessa forma o problema tem uma solução prática, uma solução ao alcance
dos poderes de qualquer ser humano. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>É também uma solução aberta
ao escrutínio crítico e ao experimento - uma indicação do quão confiante estava
o Buda na solução que ele encontrou para o problema de <i>samvega</i>. Esse é
um dos aspectos do Budismo autêntico que mais inspira confiança nas pessoas que
estão acostumadas a ouvir que elas deveriam tentar tirar das suas mentes o
insight que inspirou o seu sentimento de <i>samvega</i>. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Na verdade, o Budismo não
somente tem confiança de que pode lidar com os sentimentos de <i>samvega</i>
como também é uma das poucas religiões que os cultiva ativamente de maneira
radical. A solução para os problemas da vida demandam tanto esforço dedicado
que somente um forte <i>samvega</i> evitará que o Budista praticante regrida
aos seus antigos hábitos. Por conseguinte a recomendação para todos Budistas,
ambos homens e mulheres, leigos ou ordenados, que reflitam diariamente sobre os
fatos do envelhecimento, enfermidade, separação e morte - para desenvolver
sentimentos de <i>samvega</i> - e baseado no poder das próprias ações, conduzir
<i>samvega</i> um passo adiante, para <i>pasaada</i>. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Para aquelas pessoas cujo
sentimento de <i>samvega</i> é tão forte que elas queiram abandonar todos os
vínculos sociais que as previnem de seguir o caminho para o fim do sofrimento,
o Budismo oferece um extenso e comprovado conjunto de sabedoria no qual se
podem basear, bem como uma rede de apoio: a sangha monástica, uma instituição
que permite o abandono da sociedade leiga sem ter que se preocupar com as
questões de sobrevivência básica. Para aqueles que não podem deixar os seus vínculos
sociais, os ensinamentos Budistas oferecem um modo de vida no mundo sem ter que
se submeter ao mundo, seguindo uma vida de generosidade, virtude e meditação
para reforçar as qualidades nobres da mente que conduzirão ao fim do
sofrimento. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A relação simbiótica
desenhada para esses dois ramos da comunidade Budista ou <i>parisa, </i>garante
que cada um irá se beneficiar do contato com o outro. O apoio dos leigos
garante que os monásticos não terão que se preocupar desnecessariamente com a
alimentação, roupas e moradia; a gratidão que os monásticos inevitavelmente
sentem pela generosidade desprendida oferecida pelos leigos ajuda a evitar que
eles se transformem em misantropos. Ao mesmo tempo, o contato com os monásticos
auxilia os leigos a estimular a perspectiva adequada em relação à vida que
alimenta a energia de <i>samvega</i> e <i>pasaada</i> necessária para evitar que
elas se tornem embotadas e adormecidas pela propaganda materialista da economia
moderna.<span style="mso-spacerun: yes">  </span><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Dessa forma a atitude
Budista em relação à vida cultiva <i>samvega</i> - uma clara aceitação da falta
de sentido no ciclo de nascimento, envelhecimento e morte - e o desenvolve em <i>pasaada</i>:
um caminho seguro para o Imortal. Esse caminho inclui não somente um
ensinamento comprovado ao longo do tempo, mas também uma instituição social que
o mantém vivo. Essas são coisas que a nossa sociedade necessita muitíssimo. É
uma pena que nos esforços para popularizar o Budismo, esses são aspectos da
tradição Budista freqüentemente ignorados. Não esqueçamos de que uma das fontes
para a força do Budismo é a sua habilidade de manter um pé fora da sociedade
tradicional, e que a metáfora usada para descrever a prática é de <i>atravessar
</i>a torrente para a outra margem. Minha esperança é de que tenhamos esses
aspectos presentes na mente e no coração de tal forma que no nosso esforço em
encontrar um Budismo que seja vendável, não terminemos por vendê-lo por menos
do que vale. <o:p></o:p></span></p>


