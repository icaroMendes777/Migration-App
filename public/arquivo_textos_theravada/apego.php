<p class=Normal>Provavelmente, o termo mais mal compreendido nos círculos Budistas ocidentais é aquele normalmente traduzido como 'apego'. Demasiadas pessoas colocaram nas suas cabeças que elas não devem estar apegadas a nada. Alguns pseudo-Budistas equivocados criticam aqueles que vivem uma vida baseada em princípios morais como tendo apego aos preceitos e, assim louvam as ações imorais como um sinal de profunda sabedoria. Outros em círculos Budistas tradicionais disseminam o medo da meditação profunda por incorretamente afirmar que haverá apego aos <i>Jhanas</i>. Tudo isso vai longe demais. Talvez o auge da desinformação maldosa foi dito por Rajneesh que alegou "Eu estou tão desapegado, não estou nem mesmo apegado ao desapego" e, assim, convenientemente justificando todos os seus excessos.</p>

 <p class=Normal>   A palavra em questão em pali é <i>upadana</i>, que significa literalmente 'agarrar algo'. É comumente utilizada para indicar o 'combustível' que sustenta um processo, tal como o óleo de uma lâmpada ser o combustível/<i>upadana</i> para a chama. <i>Upadana</i> está relacionado ao desejo (<i>tanha</i>). Por exemplo, <i>tanha</i> seria estender a mão para uma deliciosa xícara de café, <i>upadana</i> seria pegar a xícara. Muito embora pensamos que podemos facilmente soltar da xícara de café pois a mão não está colada nela, ainda assim é <i>upadana</i>. Nós a pegamos. Nós a seguramos.</p>

<p class=Normal>    Felizmente nem todo tipo de apego é anti-Budista. O Buda especificou apenas quatro grupos de <i>upadana</i>: 'apego' aos cinco sentidos, 'apego' a idéias incorretas, 'apego' à idéia de que a libertação pode ser alcançada simplesmente através de preceitos e rituais, e 'apego' à idéia da existência de um 'eu'. Há muitas outras coisas que podem ser 'agarradas'  ou pelas quais pode haver apego, mas o ponto é que apenas esses quatro grupos conduzem ao renascimento, apenas esses quatro são o combustível para uma existência futura e mais sofrimento, apenas esses quatro devem ser evitados.</p>

 <p class=Normal>   Assim, adotar a prática da compaixão, adotar a prática dos cinco preceitos, ou os preceitos maiores de um monge ou monja, e adotar a prática da meditação - essas não são práticas anti-Budistas e é maldoso desencorajá-las classificando-as como 'apegos'. Observar os cinco preceitos é, de fato, o desapego dos desejos mais grosseiros como a paixão, cobiça e violência. Praticar a compaixão é o desapego do egocentrismo, e praticar a meditação é abrir mão do passado, do futuro, dos pensamentos e muito mais. A realização dos <i>Jhanas</i> é nada mais que o desapego do mundo dos cinco sentidos para obter acesso à mente. <i>Nibbana</i> é abrir mão de uma vez por todas da cobiça, da raiva e da delusão, as sementes do renascimento. O <i>parinibbana</i> é o movimento final de abrir mão do corpo e da mente (os cinco <i>khandhas</i>). É errado sugerir que qualquer um desses estágios de abrir mão são a mesma coisa que o apego.</p>

 <p class=Normal><a name=R1></a>   O caminho é como uma escada. Agarramos o degrau acima e abrimos mão do degrau abaixo para puxar-nos para cima. Logo, o degrau que acabamos de agarrar será o degrau no qual estaremos em pé. Será a hora de abrir mão desse degrau ao agarrar um degrau ainda mais alto para elevar-nos ainda mais. Se a pessoa nunca agarrou [<a href="#N1">1</a>] nada, ela irá permanecer espiritualmente obtusa.</p>

 <p class=Normal>   Para aqueles sem sabedoria, o desapego pode muitas vezes parecer apego. Por exemplo, um pássaro empoleirado no galho de uma árvore à noite parece estar firmemente agarrado ao galho, mas na verdade ele está desapegado pois está totalmente adormecido. Quando um pássaro se descontrai e os músculos começam a relaxar as suas garras se fecham no galho. Quanto mais ele relaxa, mais as garras apertam. É por isso que nunca vemos uma pássaro caindo de um poleiro, mesmo quando está dormindo. Pode parecer apego, mas, na verdade é relaxar/abrir mão. Abrir mão muitas vezes leva à quietude, não se mover de onde estamos, sendo por isso, algumas vezes, confundido como apego.</p>

 <p class=Normal>   Portanto, não se deixem influenciar por bem intencionados, mas mal informados Budistas novatos que interpretam <i>upadana</i>/apego de forma completamente equivocada. Apeguem-se sem medo aos seus preceitos, ao seu objeto de meditação e ao caminho, pois este conduz a <i>Nibbana</i>. 

