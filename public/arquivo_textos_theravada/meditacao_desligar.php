<p class=Normal>Um novo estudo realizado com base em imagens do cérebro conduzido por pesquisadores da Universidade de Yale mostrou que as pessoas que praticam meditação com regularidade são capazes de desligar as áreas do cérebro conectadas com sonhar acordado, ansiedade, esquizofrenia e outros distúrbios psiquiátricos. Os cérebros de meditadores experientes parecem mostrar menos atividade numa área conhecida como o "circuito automático no cérebro", que está ativo quando as pessoas refletem sobre temas que envolvem elas mesmas. Os pesquisadores sugeriram que através do monitoramento e supressão, ou "desligando" os pensamentos sobre a identidade, os meditadores desenvolvem um novo circuito automático, que está mais focado no presente. </p>

<p class=Normal>Dr. Judson A Brewer, médico diretor do Yale Therapeutic Neuroscience Clinic e outros colegas empregaram as imagens por ressonância magnética funcional (fMRI) para observar os cérebros de ambos 10 meditadores experientes e 13 pessoas sem experiência em meditação. Os meditadores experientes praticaram três tipos distintos de meditação:<br> <i>anapanasati</i> (concentração), <i>vipassana</i> (insight) e <i>metta</i> (amor-bondade). </p>

<p class=Normal>A equipe então empregou as imagens do fMRI para observar a atividade cerebral dos participantes enquanto estes praticavam as técnicas meditativas e quando foram instruídos para não pensarem em nada específico. </p>

<p class=Normal>Eles descobriram que os meditadores experientes, independente do tipo de meditação que praticavam, pareciam ser capazes de desligar o circuito automático no cérebro, que tem sido conectado com lapsos de atenção e distúrbios tais como déficit de atenção e hiperatividade (ADHD) e ansiedade. Mesmo quando os meditadores experientes não estavam meditando, essa região do cérebro permaneceu muito mais calma do que nos demais participantes inexperientes. Essa parte do cérebro, que compreende o córtex cingulado medial pré-frontal e posterior, também está conectada com a acumulação das placas beta amilóides da doença de Alzheimer. </p>

<p class=Normal>Dr. Brewer que pratica meditação faz 15 anos diz que a experiência com a meditação também parece otimizar o modo como o cérebro se comunica com ele mesmo. Quando o circuito automático dos meditadores experientes encontrava-se ativo, assim também estavam regiões do cérebro associadas com o auto-monitoramento e o controle cognitivo. Esse não foi o caso com os participantes inexperientes. </p>

<p class=Normal>As imagens do fMRI mostraram que a atividade cerebral dos meditadores experientes era a mesma tanto durante a meditação como quando estavam apenas descansando ou quando tinham recebido a instrução para não pensarem em nada em específico. </p>

<p class=Normal>Com base nisso os pesquisadores concluíram que talvez os meditadores experientes tenham desenvolvido um novo circuito automático, que está mais centralizado no presente do que na identidade. </p>

<p class=Normal>"Esse pessoal tem um circuito automático diferente," observou o Dr. Brewer. "Eles estão constantemente atentos ao vagueio da mente." </p>

<p class=Normal>A maioria das pessoas gasta um tempo enorme com os vagueios da mente ou sonhando acordado e as pesquisas mostram que isso tem vantagens e desvantagens cognitivas. Os cientistas mostraram que sonhar acordado pode ser positivo ao proporcionar um estímulo para a criatividade, auxiliar no processo das funções sociais e aprimorar outros processos psicológicos importantes. </p>

<p class=Normal>No entanto, <a href="meditacao_felicidade.php">estudos mais recentes</a> sugerem que uma mente que vagueia também é uma mente infeliz. Em 2010, um estudo descobriu que as pessoas relatam estarem significativamente menos felizes quando a mente está vagueando do que quando estão absorvidas por alguma tarefa. Os pesquisadores sugeriram que isso ocorre porque quando a mente vagueia, em geral estamos tomados por preocupações ao invés de viver o momento presente. </p>

<p class=Normal>Dr. Brewer também observa que um sinal que caracteriza muitas formas de doenças mentais - ansiedade, depressão, estresse pós trauma e esquizofrenia - é a preocupação com os próprios pensamentos, especificamente com os negativos. Uma série de estudos conectou esses distúrbios com hiper atividade ou conexões nervosas defeituosas no circuito automático no cérebro, a região do cérebro que mostrou menos atividade nos meditadores experientes. </p>

<p class=Normal>"Uma das coisas que a meditação parece estar fazendo é acalmando essa região do cérebro," diz o Dr. Brewer. "Isso faz todo sentido, dado aquilo que sabemos sobre o circuito automático no cérebro." </p>

<p class=Normal>A meditação não é a cura para as doenças mentais, diz o Dr. Brewer, mas ele diz que este estudo sugere que pode haver um fundamento neurológico para os benefícios relatados por muitos meditadores - mais atenção, melhor concentração e melhor habilidade para lidar com os estresses cognitivos e emocionais da vida moderna. </p>

<p class=Normal>"Por outro lado, a característica de muitas formas de doenças mentais é a preocupação com os próprios pensamentos, uma condição que a meditação parece influenciar," ele adicionou. </p>

<p class=Normal>Este estudo parece ter desvendado  algumas pistas quanto aos mecanismos neurológicos que suportam esses processos. Sua melhor compreensão poderá nos ajudar a investigar um número de enfermidades, diz o Dr. Brewer. </p>

