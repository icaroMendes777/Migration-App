<p class=Tit1 align=center style='text-align:center'>6. A Natureza das Contaminações</Tit1>



<p class=Normal>&nbsp;</p>

<hr size=2 width="100%" align=center>

<p class=Normal>&nbsp;</p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Para o ser não
iluminado, as experiências e situações são normalmente interpretadas e
avaliadas com base nas seguintes propensões ou influências:<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. A
preocupação em relação aos desejos pelos cinco tipos de objetos dos sentidos, (<i
style='mso-bidi-font-style:normal'>kama</i> – visões, sons, aromas, sabores e
sensações corporais).<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2. A
preocupação em relação à existência e preservação do eu, suas identidades
e<span style="mso-spacerun: yes">  </span>situações desejadas, (<i
style='mso-bidi-font-style:normal'>bhava</i>).<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. Idéias,
crenças e modos de pensar, (<i style='mso-bidi-font-style:normal'>ditthi</i>).<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4. Delusão
ou ignorância, (<i style='mso-bidi-font-style:normal'>avijja</i>): não
compreender de modo claro o significado das coisas como elas na verdade são,
que conduz à percepção da existência de um eu.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A terceira
e quarta condições, em particular, estão sem dúvida relacionadas: sem a
sabedoria ou entendimento, o comportamento será guiado por idéias e crenças
habituais e equivocadas. Essas duas condições abrangem áreas de influência
bastante amplas, incluindo ideais e práticas políticas, sociais e religiosas
baseadas no temperamento, hábito, treinamento e condicionamento social. Elas
estão relacionadas com as duas primeiras propensões e exercem influência sobre
estas, dessa maneira controlando todos os sentimentos pessoais e comportamento.
Elas condicionam tudo, de gostos e desgostos até os meios e métodos escolhidos
para satisfazer os desejos. A ignorância e as idéias estão ocultas
profundamente na consciência, exercendo sua influência silenciosamente e de
modo contínuo. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>De acordo
com a percepção comum, nós temos controle sobre as nossas ações e somos capazes
de ir atrás dos nossos desejos de acordo com o nosso próprio livre arbítrio.
Uma observação mais cuidadosa mostrará<span style="mso-spacerun: yes"> 
</span>que isso é uma ilusão. Se nos perguntássemos, “O que realmente queremos?
</span>Porque queremos essas coisas? <span lang=PT-BR style='mso-ansi-language:
PT-BR'>Porque agimos da forma como agimos?” não encontraremos nada que seja
realmente nosso. Encontraremos ao invés disso, padrões de comportamento
adquiridos, aprendidos na escola, através de uma educação religiosa, do
condicionamento social e outros similares. As ações individuais são simplesmente
escolhidas de dentro dos limites desses critérios e, embora possam ser feitas
algumas adaptações, elas também estarão sob a direção de outras influências. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quaisquer
escolhas, ou decisões tomadas, são parte de um fluxo de condições, e essas são
em si mesmas influenciadas por outros fatores. O que as pessoas sentem como
sendo o seu eu é nada mais do que a soma total dessas influências ou
propensões. Essas condições, além de não possuírem um eu que lhes pertença, são
forças poderosas sobre as quais a maioria das pessoas possui pouco ou nenhum
controle, de modo que existe realmente muito pouca chance para uma
verdadeira liberdade.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>As quatro
qualidades mencionadas acima são chamadas <i style='mso-bidi-font-style:normal'>asava</i>
em Pali.[<a href="#N16">16</a>] <a name=R16></a>Traduzido literalmente, <i
style='mso-bidi-font-style:normal'>asava </i>quer dizer ‘aquilo que inunda,’
porque elas ‘inundam’ a mente sempre que esta experimenta uma sensação, ou
‘aquilo que traz problemas ou que causa dor,’ porque essas coisas envenenam ou
poluem a mente nós as chamaremos de ‘impurezas.’ Não importa o que seja
experimentado, seja através de qualquer uma das portas dos sentidos ou
concebido na própria mente, essas impurezas se insinuam e espalham a sua
influência. Sensações ou pensamentos, ao invés de serem funções da mente pura,
se tornam, ao invés disso, produtos das impurezas que por sua vez poluem os
estados mentais subseqüentes e causam, como resultado, o sofrimento.<o:p></o:p></span></p>

<p class=Normal>A primeira impureza é chamada <i>kamasava</i>, a segunda, <i>bhavasava</i>,
a terceira <i>ditthasava</i> e a quarta <i>avijjasava</i>. Essas impurezas
estão por detrás do comportamento de todos os seres não iluminados. Elas criam
a delusão da idéia de um eu que é a ignorância no seu nível mais básico. Nesse
sentido elas controlam e dirigem o pensamento e o comportamento. Esse é o
primeiro nível do ciclo da Origem Dependente: a ignorância é condicionada pelas
impurezas. A partir disso o ciclo continua – com a ignorância como fator
determinante, as formações surgem como conseqüência.</p>

<p class=Normal>Enquanto estão sob a influência da delusão, a maioria das
pessoas acredita que elas mesmas estão realizando as ações, a ironia é que elas
não são de forma alguma senhoras de si mesmas – o comportamento delas é
totalmente controlado por intenções que carecem de uma consciência reflexiva.
Em essência, a ignorância é a cegueira em relação às Três Características tal
como estas são mostradas no princípio da Origem Dependente, em especial a
terceira, não-eu, (<i>anatta</i>). De modo mais específico, a ignorância é não
compreender de forma clara que as condições que em geral são consideradas como
sendo um indivíduo ou ‘ego’, ou ‘eu’, ou ‘você,’ são simplesmente um fluxo de
fenômenos físicos e mentais que surgem e cessam de modo constante, relacionados
e conectados pelo processo de causa e efeito. Esse fluxo se encontra num estado
de mudança constante. Podemos dizer que uma ‘pessoa’ é simplesmente o resultado
geral das sensações, pensamentos, desejos, hábitos, propensões, idéias,
conhecimentos, crenças e assim por diante, em um certo momento em particular,
que ou são herdados de fatores sociais e ambientais, tal como através do
aprendizado, ou formados a partir de fatores internos pessoais, todos mudando
constantemente. Não compreendendo isso com clareza, existe o apego a uma ou
outra dessas condições como sendo o eu ou pertencendo a um eu. Apegar-se às condições
dessa forma é na verdade ser enganado e controlado por elas.</p>



<p class=Normal>Isso é a “ignorância como um fator determinante para os
impulsos volitivos” num nível mais profundo do que aquele mencionado
anteriormente. Quanto aos demais elos, a partir deste ponto até <i>vedana</i>,
sensação, não deveria haver dificuldade em compreendê-los pelas explicações que
já foram dadas. Assim, passaremos desse ponto para uma outra seção importante,
“desejo, (<i>tanha</i>), como fator determinante para o apego, (<i>upadana</i>),”
mais uma das seções que lidam com <i>kilesa</i>, ou contaminações.</p>



<p class=Normal>Os três tipos de desejo que já foram mencionados são todos
expressão de um só desejo e são todos experimentados com freqüência na vida
diária, mas eles apenas podem ser vistos quando os mecanismos da mente são
analisados com cuidado. Na raiz de toda ignorância se encontra a ignorância das
coisas como um processo natural de causas e efeitos inter-relacionados que dão
origem à percepção de um eu. Isso leva a um desejo muito importante e
fundamental, o desejo de ser/existir, o desejo de sobreviver, de proteger e
preservar a ilusão de um eu. Desejar ser está relacionado com desejar ter, o
desejo não é de simplesmente existir, mas de existir para consumir aqueles
objetos que irão produzir sensações prazerosas. Portanto, pode ser dito que o
desejo pela existência depende do desejo de ter e o desejo de ter intensifica o
desejo de existir.</p>



<p class=Normal>A medida que o desejo se intensifica, várias situações podem
surgir: se o objeto desejado não for obtido no tempo desejado, <i>bhava, </i>ou
estado de existência, naquele momento se torna intolerável. A vida parecerá
difícil e isso levará ao desejo de aniquilar a situação não desejada. Ao mesmo
tempo, o desejo de adquirir novamente surgirá baseado no medo de não ser mais
capaz de experimentar sensações prazerosas, e a partir disso mais uma vez
surgirá o desejo de ser. Uma segunda possibilidade pode ser, não obter de forma
nenhuma o objeto do desejo; uma terceira, obtê-lo, mas em quantidade
insuficiente; enquanto que uma quarta pode ser, obtê-lo, mas depois desejar
outra coisa. O processo pode assumir várias formas, mas o padrão básico é
aquele de um desejo sempre crescente.</p>



<p class=Normal>Quando os mecanismos da mente são examinados em detalhe, os
seres humanos parecem estar enredados numa busca constante de um estado mais
satisfatório do que aquele que eles têm. Os seres não iluminados estão
continuamente sendo repelidos do momento presente – cada momento no presente é
um estado de estresse, uma situação insuportável. O desejo de dar fim a essa
situação, de libertar o eu do presente e encontrar um estado mais satisfatório,
surge constantemente. Desejar obter, desejar ser e desejar não ser estão
ocorrendo continuamente na vida diária dos seres não iluminados, (num nível que
poucos têm consciência). A vida pessoal, assim, se torna uma luta constante
para escapar do estado de ser presente para buscar alguma realização futura.</p>



<p class=Normal>Rastreando o processo, descobrimos que esses desejos se
originam da ignorância fundamental em relação às coisas como elas na verdade são
– em resumo, ignorância do princípio da condicionalidade e da Origem
Dependente. Essa ignorância dá origem ao equívoco básico da existência de uma
essência, de uma forma ou de outra: quer seja vendo as coisas como entidades
separadas, fixas e permanentes, (<i>sassataditthi</i>), ou vendo as coisas como
passíveis de completa e total aniquilação, (<i>ucchedaditthi</i>). Todos os
seres não iluminados têm esses dois entendimentos básicos na raiz da sua
consciência e eles dão origem aos três tipos de desejo. O desejo de existir, (<i>bhavatanha</i>),
provém da<span style="mso-spacerun: yes">  </span>percepção distorcida das
coisas como sendo entidades separadas e duradouras, (e<span
style="mso-spacerun: yes">  </span>portanto, desejáveis e cuja obtenção vale a
pena). Como alternativa, existe o equívoco de que essas entidades separadas são
passiveis de destruição, (e como tal não valendo a pena e das quais se deve
escapar), que é a base para o desejo pela aniquilação, (<a name=PVW><i>vibhavatanha</i></a>).</p>



<p class=Normal>Esses dois entendimentos incorretos básicos preparam o
caminho para o desejo. Se houvesse o entendimento do fluxo de eventos como um
processo de causas e efeitos inter-relacionados, a percepção de uma entidade
separada que perdura ou é destruída não teria fundamento. Todo desejo é<span
style="mso-spacerun: yes">  </span>baseado evidentemente nesses dois
entendimentos básicos.</p>



<p class=Normal>O medo de perder as sensações prazerosas conduz à busca
frenética por mais, e a percepção de uma entidade separada conduz à luta pela
busca dessa entidade e por sua preservação. Num nível mais grosseiro, o desejo
se expressa como o esforço pela busca de objetos do desejo, situações de vida
que proporcionam tais objetos, tédio em relação aos objetos já obtidos, e o
desespero ou inabilidade para suportar a ausência de novos objetos do desejo. O
quadro que emerge é o de pessoas incapazes de estarem em paz consigo mesmas,
ansiando continuamente por objetos do desejo e experimentando melancolia,
solidão, alienação e angústia na luta para escapar do tédio insuportável.
Quando os desejos são frustrados ocorre a decepção e o desespero.</p>



<p class=Normal>Para a maioria das pessoas a felicidade e o sofrimento
dependem totalmente de condições externas. O tempo livre se converte numa
maldição, tanto individualmente como socialmente, um motivo para o tédio,
miséria e solidão. Essa insatisfação básica aumenta proporcionalmente à quantidade de desejo e à intensidade da
busca pela gratificação sensual. Na verdade, olhando sob um ponto de vista mais
introspectivo, descobrimos que a causa mais importante para os problemas
sociais, tal como o vício em drogas e a delinqüência juvenil, é a inabilidade
das pessoas de estar em paz com o momento presente e a conseqüente luta para
escapar dele.</p>



<p class=Normal>No caso de ter estudado e treinado em alguma tradição
religiosa, e desenvolvido o entendimento correto, o desejo pode ser voltado para
uma direção boa, visando alcançar objetivos mais a longo prazo, que envolvam a
realização de boas ações e em última análise, o uso do desejo para abandonar o
desejo.</p>



<p class=Normal>A contaminação, (<i>kilesa</i>), que segue ao desejo é o
apego, do qual existem quatro tipos:</p>



<p class=Normal><b>1.</b> <b><i>Kamupadana</i></b>: <b>Apego à sensualidade.
</b>O desejo e esforço pela busca de objetos sensuais é seguido naturalmente
pelo apego e adesão. Quando um objeto do desejo é obtido, a vontade de
satisfazer ainda mais aquele desejo e o temor de perder o objeto dessa
satisfação irão produzir o apego. No caso de uma decepção e perda, o apego tem
por base a saudade. O apego se fortalece e gera mais ações em busca de
satisfação porque os objetos do desejo não proporcionam satisfação duradoura.
Como nada pode na verdade pertencer sempre ao eu, a mente está continuamente
tentando reafirmar a noção de propriedade. Dessa forma os pensamentos dos seres
não iluminados estão sempre apegados e obcecados com um ou outro objeto do
desejo. É muito difícil que uma mente assim possa ser livre e desapegada.</p>



<p class=Normal><b>2.</b> <b><i>Ditthupadana</i>: Apego a idéias</b>. O
desejo de ser ou não ser produz propensões e apego a entendimentos, teorias ou
sistemas filosóficos seguido de métodos, idéias, credos e ensinamentos. Quando existe
apego a idéias estas passam a ser identificadas como parte do eu. Assim, quando
confrontado com uma teoria ou idéia que contradiz a sua, o eu toma como uma
ameaça pessoal. O eu precisa lutar para defender a sua posição, o que por sua
vez dá origem a todo tipo de conflito. O processo tende a encurralar a mente
num espaço limitado onde o funcionamento da sabedoria fica incapacitado. Esses
pensamentos e idéias não proporcionam conhecimento, ao contrário, obstruem-no.</p>



<p class=Normal><b>3. <i>Silabbatupadana</i>: Apego a preceitos e rituais.</b>
O desejo de ser e o medo da dissolução junto com o apego a idéias, por sua vez,
conduzem ao apego cego a preceitos e rituais, tais como a magia e o ocultismo,
que, acredita-se, irão concretizar o resultado desejado. O desejo de preservação
do eu e de expressão do eu se manifestam externamente como o apego cego a modos
de comportamento, tradições, métodos, crenças e instituições. Não existe
entendimento do seu verdadeiro valor ou significado. Isso na verdade significa
que a criação dessas metodologias e práticas conduzem à rigidez e à confusão,
fazendo com que seja difícil efetivar qualquer melhoria pessoal ou derivar
algum benefício verdadeiro delas.</p>



<p class=Normal>Com relação ao tema de <i>silabbatupadana</i>, o falecido
Venerável Buddhadasa, um dos pensadores Budistas contemporâneos mais influentes
na Tailândia, deu uma explicação que pode ser de interesse no nosso caso:</p>



<p class=Normal>Praticar a virtude, ou qualquer
outra forma de prática do Dhamma, sem conhecer o seu objetivo ou refletir sobre
o seu significado, mas simplesmente acreditando que tais práticas são
auspiciosas e automaticamente produzem benefício, conduz ao apego rígido a
preceitos de acordo com crenças, hábitos ou exemplos transmitidos pela geração
anterior. Ao invés de penetrar as verdadeiras razões dessas práticas, as
pessoas simplesmente se agarram a elas através de tradições. Esse tipo de
apego, (<i>upadana</i>), é muito difícil de ser corrigido, ao contrário do
segundo tipo de apego, apego a idéias, ou pensamentos e idéias equivocados.
Este tipo de apego se fixa nas formas tangíveis da prática, nas suas aplicações
externas. [<a href="#N17">17</a>]</p>



<p class=Normal><a name=R17></a><b>4. <i>Attavadupadana</i>: Apego à idéia de
um eu.</b> O sentimento de um verdadeiro eu é a delusão no seu nível mais
básico. Existem outros fatores que intensificam esse sentimento, tais como a
linguagem e comunicações que produzem um apego a conceitos e a tendência de ver
o fluxo de fenômenos causais como entidades fixas. Esse sentimento se
desenvolve, se transformando em apego com o envolvimento do desejo. Implícito
no desejo está o apego a um eu para obter o objeto do desejo. Ambos, o desejo
de ser e o desejo de não ser dependem da percepção do eu. O medo da
desintegração intensifica o desejo de ser e a luta pela sobrevivência e
conseqüentemente a noção de um eu.</p>



<p class=Normal>O desejo depende de um eu independente e poderoso de um tipo
ou de outro. Algumas vezes parece que as coisas podem ser controladas e isso
apóia a ilusão de um eu, mas na verdade esse controle é apenas parcial e
temporário. O assim chamado eu é apenas um fator entre outros fatores
incontáveis dentro do fluxo de causa e efeito. Está além do poder de qualquer
pessoa controlar e dirigir de forma completa os objetos do apego. A sensação de
propriedade ou controle sobre as coisas pode às vezes parecer bem fundamentada,
mas ela nunca será totalmente ou completamente real, com o resultado de que o
apego e a luta para reafirmar a noção do eu é intensificada.</p>



<p class=Normal>O apego ao eu faz com que seja difícil organizar as coisas
em conformidade com o verdadeiro processo de causa e efeito. Quando as ações
não estão de acordo com a causa e efeito, e as condições não se comportam de
acordo com os desejos, o eu fica frustrado e é confrontado com a impotência e a
perda. O apego ao eu é o tipo de apego mais fundamental e é o fundamento para
todos os outros tipos.</p>



<p class=Normal>Ao experimentar sensações prazerosas, surge o apego. Isso
leva a <i>kamupadana</i>, apego a objetos sensuais desejados. <i>Ditthupadana</i>,
apego a idéias, está presente sob a forma do apego à idéia de que um objeto em
particular é bom, que só com a sua obtenção haverá felicidade, e que só os
métodos e ensinamentos que encorajam a busca e obtenção desse objeto estão
corretos. <i>Silabbatupadana</i> se manifesta como o apego aos métodos e
técnicas que são consideradas necessárias para a concretização do objetivo. <i>Attavadupadana</i>
aparece como apego ao eu que irá possuir o objeto.</p>



<p class=Normal>Em resumo, o apego causa confusão. O raciocínio dos seres
não iluminados não flui sem percalços como deveria, de acordo com a razão, mas
ao invés disso é<span style="mso-spacerun: yes">  </span>irracional, distorcido
e enrolado. O sofrimento surge do apego à idéia de um eu ou da idéia de
propriedade. Se as coisas fossem realmente o eu ou pertencessem ao eu, então
elas poderiam ser controladas de acordo com a vontade. Mas, ao invés disso,
elas seguem de acordo com causas e condições. Não estando sob o poder do
desejo, elas se tornam contrárias: o eu sofre oposição e é frustrado por elas.
Sempre que o objeto do apego é atacado, o eu também é atacado. A medida do
apego, que é a influência do ‘eu’ nas nossas ações, e a medida da perturbação
experimentada por esse eu, são todas proporcionais. O resultado é, não somente
sofrimento, mas uma vida que é vivida e operada sob o poder do desejo e do
apego, ao invés de com sabedoria e inteligência.[<a href="#N18">18</a>]</p>



<p class=Normal><a name=R18></a>Do apego o processo continua até
ser/existir, (<i>bhava</i>), nascimento, (<i>jati</i>), envelhecimento e morte,
<i>(jaramarana</i>), e a partir disso tristeza, lamentação e assim por diante,
tal como já foi explicado. Qualquer tentativa de encontrar uma saída dessa
situação é condicionada pelos padrões habituais de pensamento e é ditada pelas
propensões, preferências e idéias. Sem consciência do verdadeiro estado das
coisas, o ciclo começa uma vez mais na ignorância e segue como antes. </p>


<p class=Normal>Embora a ignorância possa ser vista como causa raiz e como
criadora de todas as demais formas de contaminações, em relação à expressão
através do comportamento, o desejo é que desempenha o papel mais dominante. É
por isso que nas Quatro Nobres Verdades é dito que o desejo é a causa do
sofrimento.</p>



<p class=Normal>Sob a influência cega e confusa da ignorância e do desejo, é
mais provável que o kamma ruim exceda o kamma bom. Mas se a ignorância for
temperada com as crenças hábeis e o pensamento correto, e o desejo for dirigido
e treinado com propósitos nobres, é mais provável que o kamma bom exceda o
kamma ruim conduzindo a resultados benéficos. Se o desejo for direcionado de
modo sábio ele se torna uma ferramenta valiosa na destruição última da
ignorância e das contaminações. O primeiro caminho é aquele do comportamento
inábil, ruim e prejudicial, enquanto que o último é o caminho da habilidade,
bondade e pureza. Tanto as pessoas boas como as ruins têm os seus próprios
tipos de sofrimento, mas apenas o caminho da bondade é capaz de conduzir à
cessação do sofrimento, à liberação e para a liberdade.</p>



<p class=Normal style='margin-left:.75in'>É o caso em que um bhikkhu, 
ouve, ‘O bhikkhu com o nome tal, dizem, eliminou as impurezas mentais, e permanece 
em um estado livre de impurezas com a libertação da mente e a libertação através da 
sabedoria, tendo conhecido e manifestado isso para si mesmo no aqui e agora.’ O 
pensamento lhe ocorre, ‘Espero que eu, também, irei – eliminar as impurezas - 
permanecendo em um estado livre de impurezas com a libertação da mente e a libertação 
através da sabedoria, tendo conhecido e manifestado isso para mim mesmo no aqui e agora.’ 
Então, mais tarde, ele abandona o desejo, tendo dependido do desejo. ‘Este corpo surge 
através do desejo. E no entanto é contando com o desejo que o desejo deve ser abandonado.’ [AN.II.145] (<a href="../sutta/ANIV.159.php">AN IV.159</a>)</p>


<p class=Normal>Se for dada a escolha entre diferentes tipos de desejo, o do
tipo bom é o incentivo preferível para a ação. No entanto, a transcendência de
ambos os desejos, bom e ruim, o caminho da sabedoria, é o caminho ideal para a
purificação, liberdade e perfeita felicidade.</p>

