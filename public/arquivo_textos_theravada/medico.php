<p class=Normal>Um mal-entendido recorrente entre alguns Budistas leigos é que um monge pode exercer a profissão de médico para os leigos. Alguns monges desenvolvem habilidades em fito terapia e outras terapias tradicionais, mas sob quais circunstâncias, se é que existe alguma, eles estão autorizados pelas suas regras a exercerem a medicina?</p>

<p class=Normal>    O Buda disse certa vez: "Qualquer um, bhikkhus, que cuide de mim, ele deveria cuidar dos enfermos" e esse enunciado bem conhecido tem sido muitas vezes usado para justificar que um monge se comporte como médico. No entanto, o enunciado é usado fora de contexto, como em breve ficará claro. O trecho completo, encontrado na seção do Vinaya Pitaka chamada Mahavagga, no capítulo 8, versículo 26, diz respeito à história do Buda que encontrou um companheiro monge que estava sofrendo com disenteria. Com a ajuda do Venerável Ananda, o Buda limpou e acalmou o monge doente. Pouco depois, o Buda se dirigiu à Sangha:</p>

<p class=Normal style='margin-left:.75in'>        "Bhikkhus, vocês não têm uma mãe nem um pai que possam cuidá-los. Se vocês, bhikkhus, não cuidarem uns dos outros, então quem poderá cuidá-los? Qualquer um, bhikkhus, que cuide de mim, ele deveria cuidar dos enfermos." </p>

 <p class=Normal>   A passagem completa deixa bem claro que quando o Buda disse: "Qualquer um que cuide de mim, ele deveria cuidar dos enfermos", o significado era para os monges cuidarem dos seus companheiros monges que estivessem doentes. Ele não estava se referindo aos monges se comportarem como médicos para os leigos.</p>

 <p class=Normal>   Na verdade, o Buda disse várias vezes que dar atendimento médico para os leigos é, para um monge, um modo de vida incorreto (<i>miccha-ajiva</i>) diretamente contrário ao quinto fator do Nobre Caminho Óctuplo e uma ação aviltante (<i>tiracchana-vijja</i>). Por exemplo, no <a href="../sutta/DN1.php">Brahmajala Sutta</a>, o Buda disse:

 <p class=Normal style='margin-left:.75in'>1.27. “’Enquanto alguns brâmanes e contemplativos, vivendo de alimentos dados em boa fé, mantêm a si mesmos através de um modo de vida incorreto (<i>miccha-ajiva</i>), através de artes aviltantes (<i>tiracchana-vijja</i>) como: prometer presentes para divindades em troca de favores; cumprir essas promessas; demonologia; ensinar feitiços que protegem as casas; induzir virilidade e impotência; consagrar terrenos para construção; dar colutórios cerimoniais e banhos cerimoniais; oferecer fogueiras de sacrifício; preparar eméticos, purgativos, expectorantes, diuréticos, curas para dor de cabeça; preparar óleo para os ouvidos, gotas para os olhos, óleo para tratamento através do nariz, colírio e antídotos; curar cataratas, praticar cirurgia, praticar como pediatra, prescrever medicamentos e tratamentos para curar efeitos colaterais - o contemplativo Gotama se abstém do modo de vida incorreto, através de artes aviltantes como essas."</p>

<p class=Normal>    Portanto, o Buda condenou de modo claro qualquer monge que tenha como parte da sua vida, a prática de medicina para os leigos.</p>

 <p class=Normal>   A tradição transmitida a todos os monges Budistas Theravada está descrita no Samantapasadika, o grande comentário do Vinaya Pitaka compilado por Buddhaghosa no Sri Lanka, no século 5 EC. Esse texto oficial define que um monge pode prescrever e fornecer medicamentos para os seus companheiros monásticos (monges e monjas), para os seus pais ou para aqueles que cuidam dos seus pais, e qualquer leigo que esteja no monastério, se preparando para ser ordenado como monge ou que esteja apenas ajudando os monges. Além disso, um monge pode prescrever, mas não comprar medicamentos, para seus irmãos e irmãs, tias e tios, avós e para quaisquer viajantes, bandidos, pessoas feridas no campo de batalha e aquelas sem parentes que venham ao monastério numa situação de emergência. Se um monge prescrever ou fornecer medicamentos além daquilo que é permitido, ele comete uma transgressão das suas regras (uma transgressão <i>dukkata</i>). Além disso, se ele prescreve ou fornece medicamentos para um leigo recebendo em troca uma recompensa material, ele incorre em outra transgressão das suas regras, aquela de "corrupção das famílias" (<i>kuladusaka</i>). Isso é o que está indicado no Samantapasadika, o comentário ao Vinaya, obedecido em todos os países Budistas Theravada.</p> 

<p class=Normal>    Esta resposta dos textos oficiais à questão "Um Monge pode exercer a Medicina?" mostra um equilíbrio sábio que reconhece o dever de um monge para com os seus pais, suas responsabilidades para com os monges e leigos que com ele convivem no seu monastério, e a compaixão para com todos aqueles que venham ao monastério numa situação de emergência. Essa regra impede que em qualquer circunstância um monge receba qualquer recompensa material por tais serviços. Além disso, lembra que o papel de um monge Budista para com os leigos não é agir como um médico para o corpo, mas agir como um sábio gentil, um médico para a mente.</p>

