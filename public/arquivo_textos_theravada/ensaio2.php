<p class=Normal>À primeira vista o Budismo
nos confronta com um paradoxo. Intelectualmente ele aparenta ser o paraíso para
o liberal: sóbrio, realista, isento de dogmas, quase científico na sua
aparência e método. Mas se entramos em contato com o Dhamma por dentro, logo
descobrimos que ele possui um outro lado que parece ser a antítese de todas as
nossas pressuposições racionais. Ainda assim não encontraremos credos rígidos
ou especulações aleatórias porém, iremos nos deparar com ideais religiosos de
renúncia, contemplação e devoção; um conjunto de doutrinas que lidam com temas
que transcendem a percepção sensorial e o pensamento e - talvez o mais desconcertante
- um programa de treinamento em que a fé figura como a virtude cardinal e a
dúvida como um obstáculo, barreira e grilhão. <o:p></o:p></span></p>

<p class=Normal>Quando tentamos estabelecer
o nosso próprio relacionamento com o Dhamma, nos encontramos no final sob o desafio
de estabelecer uma lógica para essas duas faces aparentemente irreconciliáveis:
a face empírica que se volta para o mundo e nos diz para investigar e verificar
as coisas por nós mesmos, e a face religiosa que se volta para o Além,
aconselhando-nos a dissipar as nossas dúvidas e colocar nossa confiança no
Mestre e nos seus Ensinamentos. <o:p></o:p></span></p>

<p class=Normal>Uma maneira de resolver
esse dilema é aceitar somente uma das faces do Dhamma como autêntica e rejeitar
a outra como ilegítima ou supérflua. Assim, com a tradicional piedade Budista
podemos abraçar a face religiosa da fé e da devoção, mas distanciando-nos da
visão crua do mundo e da tarefa de inquirição crítica; ou com as escusas do
Budismo moderno, podemos enaltecer o aspecto empírico do Dhamma e a sua
semelhança com a ciência porém tropeçando envergonhados sobre o seu lado
religioso. No entanto se refletimos acerca do que a genuína espiritualidade
Budista verdadeiramente requer, esclarecemos que ambas as faces do Dhamma são
igualmente autênticas e de que ambas devem ser tomadas em conta. Se falhamos em
agir dessa forma, não somente nos arriscamos a adotar uma visão distorcida do
ensinamento, mas também o nosso envolvimento com o Dhamma será provavelmente
prejudicado por parcialidade e conflito nas nossas atitudes. <o:p></o:p></span></p>

<p class=Normal>O problema no entanto
persiste, de como conciliar as duas faces do Dhamma sem se inclinar de maneira
hesitante para a auto contradição. A chave, nós sugerimos, para alcançar essa
reconciliação, e dessa forma assegurar a consistência interna para o nosso entendimento
e prática, se encontra em considerar dois pontos fundamentais: primeiro, o <i>objetivo
</i>do Dhamma, e segundo a <i>estratégia </i>empregada para atingir esse
objetivo. O <i>objetivo </i>é de alcançar a libertação do sofrimento. O Dhamma
não tem como meta nos prover de informações factuais acerca do mundo, e dessa
forma, apesar da compatibilidade com a ciência, os seus objetivos e
preocupações são distintos desta última. Primordialmente e essencialmente, o
Dhamma é um caminho para a emancipação espiritual, para libertação do ciclo de
repetição de nascimento, morte e sofrimento. Ele nos é oferecido como o
insubstituível meio de libertação, o Dhamma não busca a mera aceitação
intelectual, mas exige uma resposta que forçosamente será religiosa em sua
totalidade. Ele nos remete à verdadeira essência do nosso ser, e ali desperta a
fé, devoção e comprometimento adequados no momento em que o objetivo final da
nossa existência está em jogo.<o:p></o:p></span></p>

<p class=Normal>Mas para o Budismo a fé e a
devoção são somente estímulos que nos impelem a iniciar e perseverar ao longo
do caminho, por si mesmas elas não asseguram a libertação. A causa principal do
apego e do sofrimento, ensina o Buda, é a ignorância em relação à verdadeira
natureza da existência, dessa forma na <i>estratégia </i>Budista de libertação
o instrumento principal tem que ser a sabedoria, o conhecimento e a visão das
coisas como elas realmente são. A investigação e a inquirição crítica, fria e
descomprometida constituem o primeiro passo em direção à sabedoria, permitindo
que solucionemos as nossas dúvidas e obtenhamos uma noção conceitual acerca das
verdades das quais depende a nossa libertação. Porém a dúvida e a <a name=PVW>inquirição</a>
não podem durar indefinidamente. Uma vez que tenhamos decidido que o Dhamma
será o nosso veículo para a libertação espiritual, devemos embarcar : devemos
deixar de lado a nossa hesitação e iniciar o programa de treinamento que nos
irá conduzir da fé para a visão libertadora. <o:p></o:p></span></p>

<p class=Normal>Para aqueles que buscam no
Dhamma gratificação intelectual ou emocional, inevitavelmente ele irá mostrar
duas faces, e o resultado será um enigma. Mas se estamos preparados para
abordar o Dhamma nas suas próprias condições, como o caminho para libertação do
sofrimento, não haverá duas faces. Ao invés disso veremos aquilo que estava ali
desde o princípio: a face única do Dhamma que, como qualquer outra face,
apresenta dois lados que se complementam. <o:p></o:p></span></p>

