<p class=Normal>O Venerável Ananda foi o
ajudante pesssoal do Buda e passou vinte cinco anos servindo-o. O Buda pediu
várias vezes que ele se empenhasse com afinco e alcançasse a iluminação. Ele conhecia
todo o Dhamma e as teorias de meditação. No entanto, como ele desfrutava
acompanhar o Buda e outros bhikkhus, ele negligenciou alcançar a sua própria
iluminação até que por fim uma grande pressão foi exercida pelos 499 arahants
que se reuniram no primeiro concílio Budista. Eles insistiram que Ananda
deveria alcançar a iluminação antes da data designada para o concílio que
estava previsto para o terceiro mês após a morte do Buda.<o:p></o:p></span></p>

<p class=Normal>O Buda sempre disse:
“Bhikkhus, meditem. Não sejam negligentes. Não permitam que as suas mentes
fiquem cheias de impurezas. Não fiquem chorando e lamentando dizendo: ‘Esta
vida é cheia de problemas, cheia de miséria, cheia de dor, cheia de agonia.’” A
mente que não é desenvolvida através da prática da meditação da atenção plena
cria tensões, ansiedades e preocupações. Não fique se lamentando e repetindo os
mesmos erros. Você não poderá fugir da realidade. A vida não é cor de rosa. Ela
possui os seus altos e baixos e obstáculos a todo momento. Esse são fatos com
os quais nos defrontamos todos os dias.<o:p></o:p></span></p>

<p class=Normal>A prática da meditação da
atenção plena é parecida com os amortecedores de um carro. Se os amortecedores
não forem bons, você irá notar a dificuldade ao dirigir. Este nosso veículo – a
combinação mente e corpo – está repleto de momentos difíceis semelhantes. Não
há como escapar deles. Até mesmo se você for para a Lua (o que não é impossível
nos dias de hoje), ainda assim você irá com o seu corpo e mente repletos de
todos os impedimentos que ainda estarão presentes na sua mente. Você não
conseguiria deixá-los aqui para ir até lá. Eles lhe seguem com persistência e
teimosia aonde quer que você vá e ficam lhe importunando dia e noite. A maioria
das pessoas experimenta três soluções.<o:p></o:p></span></p>

<p class=Normal>Com a percepção de que o
problema “está lá, no mundo”, elas pensam que ao corrigir o mundo, tentando
solucionar os males da sociedade, elas poderão solucionar os seus próprios
problemas. Elas desejam tornar o ambiente “adequado, belo” e<span
style="mso-spacerun: yes">  </span>isento de problemas. E só então poderão
viver felizes. Assim, elas se interessam e algumas vezes até ficam obcecadas
pela idéia de tentar corrigir a nossa sociedade. É claro que o desejo de
melhorar os defeitos da sociedade é em si algo muito louvável. Elas vêm o
sofrimento, sentem compaixão e agem.. Elas podem manter-se totalmente ocupadas
tentando corrigir os problemas da sociedade. Elas podem pensar que assim
estarão evitando problemas para si mesmas, sem se dar conta que na verdade elas
estão se esquecendo dos problemas que as incomodam. Elas seguem sem dar atenção
para as suas dores e sofrimentos<span style="mso-spacerun: yes"> 
</span>principalmente pela falta de tempo para si mesmas. Essas pessoas são
muito compassivas, compreensivas, prontas para servir a sociedade de forma
despojada ou sem obter qualquer recompensa. Lemos muitos relatos maravilhosos
sobre muitas pessoas nobres como essas, que, à custa da sua própria iluminação,
dedicam suas vidas para a sociedade. As atividades externas podem dificultar a
solução dos nossos próprios problemas.<o:p></o:p></span></p>

<p class=Normal>Embora vivamos em sociedade
com outras pessoas, cada um de nós possui o seu mundo particular, as suas
idéias, percepções e<span style="mso-spacerun: yes">  </span>compreensão do
mundo. Cada um segue as suas próprias percepções e idéias sobre o mundo.
Algumas vezes podemos crer que todos os problemas que enfrentamos são gerados
pelo mundo exterior. Dessa forma, voltamos as nossas energias para o mundo,
acreditando que o fato de fazermos algo para corrigir a sociedade irá
solucionar os nossos problemas.<o:p></o:p></span></p>

<p class=Normal>A segunda linha de
raciocínio que as pessoas adotam para solucionar os seus problemas é pensar que
na verdade não existe nenhum problema. Elas acreditam que tudo é criado pela
imaginação. Elas pensam: “Eu sou independente, eu sou<span style="mso-spacerun:
yes">  </span>mais importante, eu estou completamente só e nada mais importa
para mim.” E a terceira forma de solucionar os problemas é fugir deles.<o:p></o:p></span></p>

<p class=Normal>Podemos obter um consolo
temporário, um conforto temporário, em pensar que o problema existe lá, no
mundo exterior, ou que o problema não existe, ou desviando a nossa atenção para
alguma outra coisa, ou ignorando que o problema exista, ou fugindo do problema.<o:p></o:p></span></p>

<p class=Normal>A verdadeira solução não se
encontra em nenhum desses métodos. A verdadeira solução, de acordo com os
ensinamentos do Buda, é descobrir uma forma de purificar o instrumento, o
agente, que faz com que o mundo seja feliz ou infeliz, calmo ou deprimente,
prazeroso ou doloroso. Aquilo que cria problemas e sofrimento para todas as
pessoas. Esse instrumento é<span style="mso-spacerun: yes">  </span>a nossa
mente. A purificação dessa mente é um dos propósitos da meditação da atenção
plena.<o:p></o:p></span></p>

<p class=Normal>Como todos sabemos, todos
os nossos pensamentos, palavras e ações se originam na mente. A mente é a
precursora. Todas as situações que experimentamos são fabricadas pela mente.
Elas são criadas na mente, dirigidas e comandadas pela mente. A mente as coloca
em ação. “Todas as ações são comandadas pela mente: a mente é o senhor delas, a
mente é quem as fabrica. Aja ou fale com um estado mental impuro que o
sofrimento virá em seguida da mesma forma como a roda da carroça segue as
pegadas do boi. Todas as ações são comandadas pela mente: a mente é o senhor
delas, a mente é quem as fabrica. Aja ou fale com um estado mental puro que a
felicidade virá em seguida, da mesma forma que a sombra acompanha o seu objeto
por toda a parte, sem nunca abandoná-lo.” (Dhammapada 1-2)<o:p></o:p></span></p>

<p class=Normal>A analogia do boi puxando a
carroça é bastante apropriada para ilustrar os nossos problemas. O boi ao puxar
a carroça não desfruta da sua tarefa. Ele não está satisfeito com a sua carga;
isso não é um prazer. Esse pobre animal puxando a carroça sofre muito. Todo o
peso da carroça está sobre os seus ombros e ele sente muita dor. Teria sido
melhor para o boi se ele não tivesse<span style="mso-spacerun: yes"> 
</span>nascido um boi. A condição do boi é comparada com a ignorância, a
estupidez – não ver a verdade como ela é. Uma vida sem iluminação é repleta de
ignorância e dada a todo tipo de impurezas. Portanto, uma pessoa não iluminada
com pensamentos, palavras e atos com a mente impura sofre<span
style="mso-spacerun: yes">  </span>como o boi<span style="mso-spacerun: yes"> 
</span>ao puxar a carroça pesada. Por outro lado, quando falamos ou fazemos
algo com a mente pura nos sentimos felizes e não temos pesar, dor ou sofrimento
nos seguindo.<o:p></o:p></span></p>

<p class=Normal>Nosso objetivo na vida
é<span style="mso-spacerun: yes">  </span>nos aprimorarmos mais a cada dia e
sermos felizes. Fazemos muitas coisas para sermos felizes. No entanto, muitas
das coisas que fazemos para sermos felizes podem gerar<span
style="mso-spacerun: yes">  </span>infelicidade, dor, sofrimento e dificuldades
porque as nossas mentes não são puras. É a mente pura que é capaz de gerar a
felicidade, não a mente impura. Portanto, o primeiro objetivo ao praticar a
meditação é de purificar a mente; isso irá gerar paz e felicidade.<o:p></o:p></span></p>

<p class=Normal>O segundo objetivo da
meditação é de superar a tristeza e a lamentação. Quando o meditador começa a
ver a verdade, ele ou ela consegue suportar e derrotar a tristeza e a
lamentação causadas pela impermanência.<o:p></o:p></span></p>

<p class=Normal>O terceiro objetivo é
superar o sofrimento e a decepção causadas pela cobiça e pela raiva.<o:p></o:p></span></p>

<p class=Normal>O quarto objetivo da
meditação é percorrer o caminho dos sábios, o caminho correto que conduz à
libertação da angústia, tristeza, desapontamento, dor e lamentação. Esse é o
caminho da atenção plena – o único caminho que nos liberta do sofrimento.<o:p></o:p></span></p>

<p class=Normal>O quinto objetivo da
meditação é de nos libertarmos completa e totalmente da dor mental e das
impurezas e de libertar a nossa mente do desejo, raiva e delusão.<o:p></o:p></span></p>

<p class=Normal>Esses cinco objetivos são
propósitos muito nobres. Todos os demais objetivos da meditação podem ser
deixados de lado porque nenhum deles será capaz de gerar os mesmos resultados e
fazer com que tenhamos realmente a paz e a felicidade através da eliminação de
todos os nossos problemas. Nós não tentamos ignorar ou evitar os nossos
problemas mas, através da atenção plena nós os enfrentamos e lidamos com eles à
medida que surjam nas nossas mentes.<o:p></o:p></span></p>

<p class=Normal>Algumas pessoas querem
simplesmente meditar sem ter qualquer conhecimento básico de meditação. Elas
pensam que o conhecimento da teoria de meditação é um obstáculo. Essa atitude
pode ser comparada à atitude de um viajante que quer alcançar um destino
definido – digamos a cidade de Washington. O viajante possui grande confiança
na sua habilidade e acredita que apenas a sua confiança é suficiente para
levá-lo até o seu destino. Pode ser que essa pessoa possua um meio de
transporte, um carro. Então, entrando no carro, ela começa a dirigir. No
entanto, não houve nenhuma preparação para a viagem. Ela desconhece as estradas
ou as condições em que as estradas se encontram ou a previsão do tempo. Ela nem
mesmo consultou um mapa. Tudo que ela possui é um carro e a sua confiança, e
alguma experiência em dirigir. O carro pode estar em ordem e com o tanque
cheio, a pessoa entra no carro e começa a dirigir. Ela pode permanecer na
estrada por um bom tempo, gastando uma grande quantia em dinheiro com gasolina,
gastando tempo e energia. De fato, dirigindo ela chegará em algum lugar, mas
não necessariamente ao seu destino. Um motorista sábio por outro lado, estuda o
mapa em detalhe, determina quais são os desvios e indaga de outras pessoas que
são mais experientes.<o:p></o:p></span></p>

<p class=Normal>Se o motorista desejar ir
para Washington e se houver um lugar chamado Washington, o motorista irá
encontrar esse lugar. De forma semelhante, precisamos ter um objetivo na
meditação. Queremos alcançar esse objetivo e realizar o nosso intento. E
necessitamos de algum tipo de orientação. Não necessitamos necessariamente
receber uma grande dose de teoria filosófica e especulativa. A orientação
funciona como placas na estrada, que devemos seguir de forma a sabermos (sem
ter que adivinhar) se estamos indo na direção correta. Certamente, a confiança
é necessária, mas, só ela não é suficiente. Além disso, precisamos ter a
compreensão e o entendimento da teoria.<o:p></o:p></span></p>

<p class=Normal>Mas então o que é a
meditação? Como alcançamos esse objetivo de purificar a mente, superando a
tristeza e a lamentação, superando a dor e o desapontamento, como percorremos o
caminho que conduz à libertação da dor, sofrimento e samsara – este mundo de
nascimento e morte?<o:p></o:p></span></p>

<p class=Normal>Existe uma forma de
realizar isso. Quando nos referimos ao “caminho”, pode ser que muitas pessoas
percam o interesse. Pode ser que elas pensem que o orador esteja tentando
vender algo e tentando depreciar todo o restante do mundo e digam, “Se esse é o
único caminho, nós não estamos dispostos a comprá-lo.” Agora, quando você quer
ir para Washington, existem várias formas de chegar lá. Voar, é claro, é a
maneira mais rápida nos dias de hoje. Em outras épocas usaríamos um carro ou um
barco ou os nossos próprios pés. Qualquer que seja o meio de transporte,
teremos de percorrer uma distância específica para chegar em Washington. O
essencial é que cheguemos lá – quer seja por meios lentos ou ligeiros.
Portanto, o “caminho” significa o “caminho da atenção plena” que percorre uma
certa distância para que<span style="mso-spacerun: yes">  </span>cumpramos o
nosso objetivo.<o:p></o:p></span></p>

<p class=Normal>Esse caminho da atenção
plena, no entanto, não está localizado em uma área geográfica ou no espaço. Ele
se encontra na nossa mente. Temos que fazer certas coisas. Esse “fazer” também
é o “caminho” – a forma como cultivamos as nossas mentes para completar essa
jornada. Cultivar a mente significa praticar a atenção plena. Quando a atenção
plena não está presente, quando estamos desatentos o tempo todo, estamos
aprisionados em um embuste. Estamos presos a todo tipo de confusão. Não
compreendemos as coisas como na verdade elas são. Para fazer com que cheguemos
ao nosso destino necessitamos um entendimento claro de onde nos encontramos. O
claro entendimento nasce através da atenção plena. Não importa as outras coisas
que façamos ou as outras práticas às quais nos dediquemos, elas possuem os seus
próprios objetos e propósitos. Aprendemos que elas não purificam a mente.<o:p></o:p></span></p>

<p class=Normal>A própria palavra meditação
significa cultivo. Sabemos o que se quer dizer com “nós cultivamos a terra.”
Sabemos <span style='mso-tab-count:1'>      </span>que tem que haver um pedaço
de terra e meios de cultivá-la. Precisamos fazer certas coisas, tal como cortar
as árvores para limpar a terra, remover as ervas daninhas e outras coisas mais,
ará-la repetidas vezes e fertilizá-la. Depois podemos plantar as sementes e
alimentá-las para que as plantas se desenvolvam. De forma semelhante com a
prática de meditação, necessitamos intencionalmente cultivar a mente. Nós não
nos sentamos em um lugar apenas esperando que algo aconteça. Podemos esperar
indefinidamente ou por um longo período de tempo, sem que nada aconteça.
Poderemos dizer que passamos um certo tempo meditando. Ficar sentado em um
lugar sem fazer nada não é meditar. E, da mesma forma, ficar observando a nossa
respiração todo o tempo é inadequado e insuficiente. É evidente que a atenção
plena na respiração é uma parte importante da meditação. A simples observação
da respiração sem a aplicação da atenção plena pode ser caracterizada como a
prática da meditação da tranqüilidade, mas, se não houver atenção plena não é a
Concentração Correta. Começamos, no entanto, observando a nossa respiração.
Essa meditação que é absolutamente particular ao Budismo é denominada meditação
Vipassana ou Insight. A orientação para a prática da meditação Vipassana está
disponível no <a href="../sutta/MN10.php">Satipatthana Sutta</a> (Os
Fundamentos da Atenção Plena).<o:p></o:p></span></p>

<p class=Normal>Os quatro fundamentos da
atenção plena são: a atenção plena no corpo, a atenção plena nas sensações, a
atenção plena na mente e a atenção plena nos objetos mentais.Explicaremos um
por um.<o:p></o:p></span></p>

<p class=Normal>Vejamos a primeira parte –
a atenção plena no corpo. A atenção plena no corpo está dividida em seis
seções. A primeira delas é a atenção plena na respiração. Agora, porque a
respiração é incluída como parte da atenção plena no corpo? A respiração é
parte do corpo. Este corpo, da forma como o conhecemos, é composto de quatro
elementos básicos: o elemento da extensão (partes sólidas), o elemento da
coesão (partes líquidas), o elemento do calor (irradiação) e o elemento do ar
(oscilação ou movimento). Portanto, quando tentamos praticar a atenção plena no
corpo começamos com a atenção plena na respiração que é o elemento do ar.<o:p></o:p></span></p>

<p class=Normal>Nessa meditação, não nos colocamos
em uma terra da fantasia imaginária. Não tentamos induzir a auto-hipnose. Não
tentamos descobrir os elementos místicos ocultos do universo. Não tentamos ser
absorvidos pelo universo. Não tentamos ser “um” com todo o universo. Tudo isso
são apenas palavras interessantes. Tentamos usar a nossa própria identidade:
nosso corpo e mente. Observamos com atenção plena este corpo e mente e as suas
atividades, investigamo-los porque são eles que carregamos conosco por toda
parte. Este corpo e mente são o nosso laboratório. Tudo que necessitamos para o
nosso trabalho se encontra aqui – a matéria prima, a substância química, os
gases, calor, ar, água, expansão – está tudo aqui. É neste corpo, nesta
identidade, que encontramos tudo isso. Meu laboratório é a minha mente e meu
corpo. Sempre tento observá-los. Não posso realizar o meu trabalho no seu
laboratório. Você deve trabalhar no seu laboratório. Muitos de nós esquecemos
dos nossos laboratórios e tentamos trabalhar no laboratório de outrem. Tentamos
descobrir o que tal pessoa está fazendo, o que ela está comendo, com quem ela
está se associando, aonde ela está indo, o que ela está lendo, quanto dinheiro
ela tem, etc.. Sempre nos esquecemos dos nossos laboratórios. Pode ser que
nunca venhamos a saber o que se encontra nesse nosso laboratório. Nós, nessa
prática de meditação Vipassana, nos tornamos introspectivos, atentos e
cuidadosos ao observar o que está acontecendo aqui nesta mente e corpo no
momento presente. Isso é a meditação Vipassana; a observação metódica neste
nosso laboratório.<o:p></o:p></span></p>


