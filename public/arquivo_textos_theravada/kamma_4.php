<p class=Tit1 align=center style='text-align:center'>4. Kamma no nível social</Tit1>

<p class=Tit2>Conteúdo:<o:p></o:p></span></Tit2>

<p class=Normal style='margin-left:.75in'> <a href="#T41">A importância de <i>ditthi</i> na criação de kamma</a><o:p></o:p></span></p>

