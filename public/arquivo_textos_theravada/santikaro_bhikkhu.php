<p class=Tit1 align=center style='text-align:center'>Santikaro Bhikkhu</Tit1>

<p class=Normal>Santikaro Bhikkhu é um monge americano nascido em Chicago no dia 11 de Junho de 1957. Ele se formou em Retórica pela Universidade de Illinois em 1980 sendo ordenado como bhikkhu no dia 28 de Janeiro de 1985 no Wat Cholapratan Rangsarit, Pak Kred, Nontaburi, ao norte de Bangcoc, Tailândia, pelo Venerável  Panyananda Bhikkhu.<o:p></o:p></span></p>

<p class=Normal>Em Abril de 1985 ele foi para o monastério Suan Mokkhabalarama, fundado por  Buddhadasa Bhikkhu, um eminente mestre, erudito e reformador tailandês. Santikaro se tornou o principal tradutor para o inglês das obras de Ajaan Buddhadasa. Mais tarde ele também passou a auxiliar o abade de Suan Mokkh, Ajaan Poh Buddhadhammo nos  retiros mensais de meditação, tendo liderado a maioria desses retiros de 1986 até 1993. A partir de 1990 ele também conduziu retiros de meditação no Sri Lanka, Índia, Estados Unidos da América, Filipinas e Austrália. Ele também desempenhou o papel de abade do Suan Atammayatarama, um centro de treinamento para monges estrangeiros em Suan Mokkh.<o:p></o:p></span></p>

<p class=Normal>O Ven. Santikaro traduziu e editou Mindfulness with Breathing, Heartwood of the Bodhi Tree e vários outros textos menores. Com participação ativa no diálogo inter-religioso e nas atividades do Budismo engajado socialmente; ele também está envolvido com a pesquisa e crítica das ciências cognitivas de estudos da consciência, pesquisas modernas sobre o cérebro e outros campos de atividade relacionados, sob a perspectiva dos ensinamentos Budistas.<o:p></o:p></span></p>

