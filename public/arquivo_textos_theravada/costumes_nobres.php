<p class=Normal>Ao longo da sua história o
Budismo tem atuado como uma força civilizadora. Os seus ensinamentos sobre
karma por exemplo - o princípio de que todas as ações intencionais têm
conseqüências - têm ensinado virtude e compaixão para muitas sociedades. Mas
num nível mais profundo, o Budismo sempre esteve dividido entre a civilização e
os lugares desabitados. O próprio Buda conquistou a iluminação numa floresta,
fez o seu primeiro sermão numa floresta e morreu numa floresta. As qualidades
mentais que ele precisou ter, de modo a sobreviver fisicamente e mentalmente ao
ir, desarmado, para uma região selvagem, foram essenciais na sua descoberta do
Dhamma. Elas incluíam a resistência, determinação e atenção plena; honestidade
consigo mesmo e circunspecção; estabilidade em face à solidão; coragem e
ingenuidade em face aos perigos externos; compaixão e respeito pelos demais
habitantes da floresta. Essas qualidades formaram a &quot;cultura caseira&quot;
do Dhamma.<o:p></o:p></span></p>

<p class=Normal>De tempos em tempos, à
medida que o Budismo se espalhava e se adaptava a diferentes sociedades, alguns
praticantes sentiam que a mensagem original do Dhamma havia se diluído. Assim
eles retornavam para as florestas com o propósito de reviver a cultura caseira.
Muitas tradições de florestas ainda estão vivas nos dias de hoje, especialmente
nos países Theravada do Sudeste Asiático e no Sri Lanka. Nesses países, monges
ascetas mendicantes continuam a perambular pelas florestas tropicais que ainda
restam, em busca da iluminação no mesmo ambiente em que o próprio Buda
encontrou a sua iluminação. Entre essas tradições de florestas, aquela que tem
atraído o maior número de estudantes do Ocidente e que começa a fincar raízes
no Ocidente, é a Tradição de Florestas Kammatthana (Meditação) da Tailândia. <o:p></o:p></span></p>

<p class=Normal>A tradição Kammatthana foi
fundada por Ajaan Mun Bhuridatto nas primeiras décadas deste século (XX). O
estilo de prática de Ajaan Mun era solitário e rigoroso. Ele seguia o <i>Vinaya
</i>(disciplina monástica) com fidelidade e também observava muitas das que são
conhecidas como as treze clássicas práticas <i>dhutanga </i>(ascéticas), tal
como viver de comida dada por caridade, vestir-se com mantos feitos de trapos,
morar na floresta, comer somente uma refeição por dia. Buscando lugares
isolados nas florestas da Tailândia e do Laos, ele evitava as responsabilidades
da vida monástica e passava longas horas do dia e da noite em meditação. Apesar
da sua natureza solitária, ele atraiu um grande número de estudantes dispostos
a enfrentar as dificuldades da vida na floresta, para poder estudar com ele. <o:p></o:p></span></p>

<p class=Normal>Ele também tinha os seus
detratores, que o acusavam de não seguir os hábitos tradicionais do Budismo
Tailandês. Ele normalmente respondia dizendo que não estava interessado em
curvar-se aos costumes de alguma sociedade em particular - já que eles eram,
por definição, os costumes de pessoas com desejos, raiva e delusão nas suas
mentes. Ele estava mais interessado em encontrar e seguir a cultura caseira do
Dhamma, ou aquilo que ele denominava os costumes dos nobres: as práticas que
permitiram que o Buda e os seus discípulos alcançassem a iluminação. Essa frase
- os costumes dos nobres - vem de um inncidente na vida do Buda: não muito tempo
depois da sua iluminacão, ele retornou para a sua cidade natal para ensinar o
Dhamma para a família que ele havia abandonado seis anos antes. Depois de
passar a noite em uma floresta, ele foi, ao raiar do dia, para a esmola de
alimentos na cidade. O seu pai, o rei, soube disso e imediatamente saiu para
criticá-lo. &quot;Isso é vergonhoso&quot;, disse o rei. &quot;Ninguém na
linhagem da nossa família alguma vez saiu a mendigar. É contrário aos nossos
costumes familiares.&quot; <o:p></o:p></span></p>

<p class=Normal>&quot;Majestade&quot; o
Buda respondeu, &quot;eu agora pertenço não à linhagem da minha família, mas à
linhagem dos nobres. São os costumes deles que eu sigo.&quot; <o:p></o:p></span></p>

<p class=Normal>Ajaan Mun dedicou muitos
anos da sua vida em busca desses costumes. Nascido em 1870, filho de
agricultores de arroz na província de Ubon no nordeste da Tailândia, ele foi
ordenado como monge na capital da província em 1892. Na época da sua ordenação
haviam dois tipos de Budismo disponíveis na Tailândia. O primeiro pode ser
chamado de Budismo Costumeiro - os costumes e rituais transmitidos ao longo dos
séculos, de mestre para mestre, com pouca ou nenhuma referência ao Cânone em
Pali. Na sua maior parte, esses costumes ensinavam os monges a viver uma vida
sedentária no monastério do vilarejo, servindo aos moradores do vilarejo como
médicos ou adivinhadores da sorte. A disciplina monástica tendia a ser
relaxada. De vez em quando, os monges partiam em uma peregrinação que eles
chamavam de ‘dhutanga’ que tinha pouca semelhança com as práticas clássicas de
dhutanga. Ao invés disso, era mais uma válvula de escape indisciplinada para as
pressões da vida sedentária. Além disso, monges e pessoas leigas praticavam
formas de meditação que se desviavam do caminho da tranqüilidade e insight
delineado no Cânone em Pali. As suas práticas, denominadas <i>vichaa aakhom,</i>
ou rituais de recitação de palavras mágicas, envolviam iniciações e invocações
usadas com objetivos xamanistas, tais como feitiços protetores e poderes
mágicos. Eles raramente mencionavam nirvana, exceto como uma entidade a ser
invocada para os ritos xamanistas. <o:p></o:p></span></p>

<p class=Normal>O segundo tipo de Budismo disponível
na época era o Budismo Reformista, baseado no Cânone em Pali e que teve início
por volta de 1820 com o Príncipe Mongkut, que mais tarde se tornou o Rei Rama
IV (que foi retratado na peça musical <i>O Rei e Eu). </i>O Príncipe Mongkut
esteve ordenado como monge por vinte sete anos antes da sua ascensão ao trono.
Após estudar o Cânone durante os seus primeiros anos como monge, ele ficou
desapontado com o nível de prática que viu nos monastérios Tailandeses. Assim
ele se reordenou com os Mons - um grupo étnico que ocupava a fronteira entre a
Tailândia e Myanmar e que também habitavam em alguns vilarejos na margem oposta
de Bangcoc - e estudou o Vinaya e as práticas clássicas de dhutanga sob a
orientação de um mestre Mon. Mais tarde, seu irmão, o rei Rama III, queixou-se
de que era uma desgraça que um membro da família real se unisse a uma minoria
étnica e então construiu um monastério na margem do rio do lado de Bangcoc,
para o Príncipe-Monge. Ali, Mongkut atraiu um pequeno, mas sólido grupo de
monges e patrocinadores leigos que pensavam da mesma forma e assim nasceu o
movimento Dhammayut (lit., <i>De Acordo com o Dhamma</i>). <o:p></o:p></span></p>

<p class=Normal>Nos seus primeiros anos, o
movimento Dhammayut era um grupo informal dedicado aos estudos em Pali, focando
no Vinaya, nas práticas clássicas de dhutanga, na interpretação racionalista do
Dhamma e na restauração das técnicas de meditação ensinadas no Cânone em Pali,
tais como, <i>anussati </i>(contemplação das qualidades especiais do Buda) e a
atenção plena no corpo. Nenhum dos membros do movimento, no entanto, pode
provar que os ensinamentos do Cânone em Pali, na verdade, conduziam à
iluminação. O próprio Mongkut estava convencido que o caminho para nirvana não
estava mais aberto, mas ele sentia que uma grande quantidade de méritos poderia
ser obtida, revivendo, pelo menos, as formas externas das tradições mais
antigas do Budismo. Tomando, formalmente, o voto de um bodisatva, ele dedicou
os méritos dos seus esforços a um futuro estado de Buda. Muitos dos seus
estudantes também tomaram votos, na esperança de virem a ser discípulos desse
futuro Buda. <o:p></o:p></span></p>

<p class=Normal>Ao abandonar o hábito e
subir ao trono, após a morte do seu irmão em 1851,<span style="mso-spacerun:
yes">  </span>Rama IV ficou em condições de impor as suas reformas ao restante
da Sangha Tailandesa, mas ele preferiu não fazer isso. Ao invés disso, ele
discretamente patrocinou a construção de novos centros Dhammayut na capital e
nas províncias e foi assim que - na época de Ajaan Mun - existia um punhado de
monastérios Dhammayut em Ubon. <o:p></o:p></span></p>

<p class=Normal>Ajaan Mun sentia que o
Budismo Costumeiro tinha pouco a oferecer e assim ele ingressou na ordem
Dhammayut, tomando um aluno do Príncipe Mongkut como seu preceptor. Ao
contrário de muitos que ingressavam na ordem naquele tempo, ele não tinha
interesse no progresso social que viria com o estudo acadêmico e as nomeações
eclesiásticas. Ao invés disso, como<span style="mso-spacerun: yes">  </span>sua
vida no campo havia deixado nele uma forte impressão dos sofrimentos inerentes
ao ciclo de vida e morte, o seu único objetivo era encontrar um caminho para
sair do ciclo. Como resultado, em pouco tempo ele abandonou o ambiente
acadêmico do templo do seu preceptor e foi viver com um mestre chamado Ajaan
Sao Kantasilo (1861-1941) num pequeno monastério de meditação nos arredores da
cidade. <o:p></o:p></span></p>

<p class=Normal>Ajaan Sao era uma pessoa
incomum na ordem Dhammayut, no sentido de que ele não tinha interesses
acadêmicos, mas se dedicava à prática da meditação. Ele treinou Ajaan Mun com a
disciplina rígida e as práticas meditativas do Cânone, dentro do contexto dos
perigos e solidão das florestas. Ele não podia garantir que essa prática
conduziria às nobres realizações, mas ele acreditava que ela ia na direção
certa. <o:p></o:p></span></p>

<p class=Normal>Após perambular por vários
anos com Ajaan Sao, Ajaan Mun saiu, por conta própria, em busca de um mestre
que pudesse com certeza mostrar o caminho para as nobres realizações. A sua
busca consumiu quase duas décadas e envolveu incontáveis dificuldades enquanto
viajava através das florestas do Laos, da região central da Tailândia e da Birmânia,
mas ele nunca encontrou o mestre que procurava. Pouco a pouco, ele foi
compreendendo que teria que seguir o exemplo do Buda e tomar a própria floresta
como seu mestre, não simplesmente se adequando à natureza - pois a natureza é o
próprio samsara - mas de superá-la para chegar às verdades que a transcendem
completamente. Se ele queria encontrar o caminho para superar o envelhecimento,
enfermidade e morte, ele teria que aprender as lições em um ambiente em que o
envelhecimento, enfermidade e morte são colocados em agudo contraste. Ao mesmo
tempo, os seus encontros com outros monges na floresta o convenceram de que
aprender as lições da floresta, envolvia mais do que simplesmente dominar as
habilidades necessárias para a sobrevivência física. Ele também precisaria
desenvolver a acuidade para não ser enganado pelos desvios sem saída na sua meditação.
Assim, com uma profunda noção da imensidão da sua tarefa, ele retornou para a
região montanhosa, na parte central da Tailândia e se estabeleceu sozinho em
uma caverna.<o:p></o:p></span></p>

<p class=Normal>Durante o longo período de
treinamento nas florestas, Ajaan Mun descobriu que - ao contrário das crenças
Reformistas e Costumeiras - o caminho para nirvana não estava fechado. O
verdadeiro Dhamma seria encontrado não em costumes antigos ou textos mas num
coração e mente bem treinados. Os textos eram indicadores para o treinamento, nada
além disso. As regras do Vinaya, ao invés de serem simples costumes exteriores,
desempenhavam um papel importante na sobrevivência mental e física. Quanto aos
textos sobre o Dhamma, a prática não consistia em apenas confirmar o que eles
dizem. Ler e pensar sobre os textos não proporcionaria um entendimento adequado
do que eles querem dizer - e não tinha valor, mostrar um verdadeiro respeito
por eles. Verdadeiro respeito pelos textos significava tomá-los como um
desafio: colocar os seus ensinamentos à prova, de maneira séria, para ver se,
de fato, eles são verdadeiros. Durante o processo de teste dos ensinamentos, a
mente iria encontrar muitos entendimentos que não estavam contidos nos textos.
Estes, por seu turno, também tinham que ser testados, de forma que se aprendia
gradualmente por tentativa e erro até o ponto de alcançar a nobre realização.
Somente então, diria Ajaan Mun, a pessoa compreenderia o Dhamma. <o:p></o:p></span></p>

<p class=Normal>Essa atitude em relação ao
Dhamma corresponde àquilo que antigas culturas denominavam ‘conhecimento do
guerreiro’ - o conhecimento que surge ao desenvolver habilidades em situações
difíceis - oposto ao ‘conhecimento do escriba’ que as pessoas sentadas em
relativa segurança e tranqüilidade podem colocar sob a forma de palavras. É
claro que guerreiros precisam usar palavras no seu treinamento, mas eles vêm
confiabilidade em um texto apenas se os seus ensinamentos são sustentados na
prática. O próprio Cânone encoraja essa atitude ao mencionar o Buda ensinando a
sua tia, &quot;Quanto às qualidades que você provavelmente conhece. 'Essas
qualidades conduzem ao desapego, não à cobiça; a estar livre dos grilhões, não
a estar agrilhoada; à renúncia, não ao acúmulo; à modéstia, não ao
engrandecimento pessoal; à satisfação, não à insatisfação;<span
style="mso-spacerun: yes">  </span>ao isolamento, não ao enredamento; a
estimular a energia, não à preguiça; a não ser um incômodo, não a ser um
incômodo: Você deve definitivamente entender, ' Isto é o Dhamma, isto é o
Vinaya, essas são as instruções do Mestre.'&quot; <o:p></o:p></span></p>

<p class=Normal>Portanto, a autoridade máxima,
para julgar um ensinamento, não está no fato do ensinamento poder ou não ser
encontrado em um texto. Ela se encontra na rígida honestidade de cada pessoa em
colocar o Dhamma à prova e cuidadosamente monitorar os resultados. <o:p></o:p></span></p>

<p class=Normal>Quando Ajaan Mun alcançou o
ponto em que podia garantir que o caminho para as realizações nobres ainda
estava aberto, ele retornou para a região nordeste da Tailândia para informar
Ajaan Sao, para então continuar perambulando. Pouco a pouco ele começou a
atrair uma multidão de seguidores. As pessoas que o conheciam ficavam
impressionadas com o seu comportamento e ensinamentos, que eram diferentes
daqueles dos demais monges que elas haviam conhecido. Elas acreditavam que ele
incorporava o Dhamma e Vinaya em tudo aquilo que fazia e dizia. Como mestre, ele
adotava a abordagem de um guerreiro no treinamento dos seus alunos. Ao invés de
simplesmente transmitir conhecimentos verbais, ele os colocava em situações em
que teriam que desenvolver as qualidades, mentais e de caráter, necessárias
para a sobrevivência na batalha contra as suas próprias contaminações
(mentais). Ao invés de ensinar uma única técnica de meditação, ele lhes
ensinava um arsenal de habilidades - como disse um aluno, &quot;Tudo, desde como
lavar as escarradeiras&quot; - e depois mandava que fossem para as florestas. <o:p></o:p></span></p>

<p class=Normal>Foi após o retorno de Ajaan
Mun para o nordeste que um terceiro tipo de Budismo proveniente de Bangcoc -
Budismo do Estado - começou a invadir a sua vida. Em um esforço para apresentar
uma frente unida face à ameaça imperialista da Grã Bretanha e França, Rama V
(1868-1910) queria mudar o país, de um sistema feudal livre para um sistema
centralizado de nação-estado. Como parte desse programa, ele e seus irmãos - um
dos quais era um monge - decretaram reformas religiosas para prevenir a intrusão
de missionários cristãos. Tendo sido educados por tutores britânicos, eles
criaram um novo currículo monástico que sujeitava o Dhamma e o Vinaya a noções
Vitorianas de razão e utilidade. A sua nova visão do Vinaya, por exemplo, era
um compromisso entre o Budismo Costumeiro e o Reformista, desenhada para se
opor a ataques Cristãos de que os monges eram irresponsáveis e preguiçosos. Os
monges foram instruídos a abandonar as perambulações, fixar-se em monastérios
estabelecidos e aceitar o novo currículo do estado. Como os monges Dhammayut
eram aqueles com o melhor nível de educação na Tailândia naquela época - e
possuíam as ligações mais próximas com a família real - eles foram convocados
para realizar o trabalho avançado, para o governo, nas regiões mais remotas. <o:p></o:p></span></p>

<p class=Normal>Em 1928, uma autoridade do
Dhammayut, que não era favorável à meditação e às perambulações pelas
florestas, assumiu o controle dos assuntos religiosos na região do nordeste. Na
tentativa de domesticar os seguidores de Ajaan Mun, ele ordenou que eles
estabelecessem monastérios e auxiliassem na divulgação do programa do governo.
Ajaan Mun e um punhado de<span style="mso-spacerun: yes">  </span>alunos seus,
partiram em direção ao norte, onde ainda tinham liberdade para perambular. No
início da década de 1930, Ajaan Mun foi nomeado abade de um importante
monastério na cidade de Chieng Mai, mas fugiu do lugar antes do amanhecer, no
dia seguinte. Ele voltou a se estabelecer no nordeste somente nos últimos anos
da sua vida, depois que as autoridades eclesiásticas locais tenderam a uma
atitude mais favorável em relação à sua forma de prática. Ele manteve muitas
das suas práticas dhutanga até a sua morte em 1949. <o:p></o:p></span></p>

<p class=Normal>Foi somente em 1950 que o
movimento que ele fundou obteve aceitação em Bangcoc e apenas na década de 1970
é que obteve proeminência em escala nacional. Isso coincidiu com uma perda
generalizada de confiança nos monges do estado, muitos dos quais eram pouco
mais do que burocratas vestidos com mantos. Como resultado, os monges
Kammatthana passaram a representar, aos olhos de muitos monásticos e pessoas
leigas, uma expressão sólida e confiável do Dhamma, num mundo em rápida e
violenta modernização. <o:p></o:p></span></p>

<p class=Normal>A história Budista tem
mostrado que as tradições de florestas passam por um ciclo de vida muito curto.
À medida que uma perde a sua força, outra surge no seu lugar. Mas, com a
destruição em massa das florestas na Tailândia nas últimas décadas, a tradição
Kammatthana pode vir a ser a última grande tradição de florestas que a
Tailândia irá produzir. Felizmente, nós, do Ocidente, conseguimos ter acesso a
essa tradição, em tempo de colher lições, que irão nos auxiliar a cultivar os
costumes dos nobres no Ocidente e estabelecer a nossa própria tradição de
florestas. <o:p></o:p></span></p>

<p class=Normal>Talvez, a mais importante
dessas lições se refira ao papel que as florestas desempenham em testar e
corrigir tendências que se desenvolvem entre os Budistas, nas metrópoles e
cidades. A história da tradição Kammatthana desmente a noção simplista de que o
Budismo tem sobrevivido somente através da adaptação à cultura local. A
sobrevivência do Budismo e a sobrevivência do Dhamma são duas coisas
diferentes. Pessoas como Ajaan Mun - dispostas a fazer qualquer sacrifício que
seja, para descobrir e praticar o Dhamma em seus próprios termos - são aqueles
que têm mantido o Dhamma vivo. É claro que as pessoas sempre tiveram a
liberdade para se engajar nas tradições Budistas da forma como quisessem, mas
aquelas que mais se beneficiaram desse engajamento são aquelas que ao invés de
transformar o Budismo para que ele se adapte às suas preferências, transformaram
a si mesmas para se adequar aos costumes e tradições dos nobres. Encontrar
esses costumes não é fácil dada a desconcertante variedade de tradições que os
Budistas geraram ao longo dos séculos. Para testá-las cada indivíduo tem que
recorrer aos seus próprios poderes de firme honestidade, integridade e sabedoria.
Não existem garantias fáceis. E talvez, esse fato por si mesmo, seja uma medida
do verdadeiro valor do Dhamma. Somente as pessoas com verdadeira integridade
são capazes de realmente compreendê-lo. Como certa vez disse Ajaan <a name=PVW>Lee</a>,
um dos alunos de Ajaan Mun, &quot;Se a pessoa não for verdadeira com os
ensinamentos do Buda, os ensinamentos do Buda não serão verdadeiros para com
essa pessoa - e essa pessoa não será capaz de entender quais são os verdadeiros
ensinamentos do Buda.&quot; <o:p></o:p></span></p>

