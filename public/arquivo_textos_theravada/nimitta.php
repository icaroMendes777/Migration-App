<p class=Normal><b>INTRODUÇÃO</b></p>

<p class=Normal>Como o título deste ensaio sugere, uma importante charada
tem de ser solucionada por qualquer meditador ou estudioso que tentar entender
de modo claro as qualidades da experiência que acompanham a transição da mera
atenção na respiração até a completa imersão na consciência dos jhanas.
Mostrarei que existem bons motivos para confusão a esse respeito quando alguém
investiga a evolução histórica das descrições nos comentários, desde o <i>Patisambhidamagga</i>
passando pelo <i>Vimuttimagga</i> até o (mais recente) <i>Visuddhimagga</i>.</p>

<p class=Normal>Visto que o <i>Visuddhimagga</i> exerce tanta influência e é
freqüentemente mencionado pelos mestres modernos, parece crítico que ele seja
confiável, e se em alguns aspectos assim não for, então, com o respaldo de
evidências, deve-se mostrar claramente porque não é.</p>

<p class=Normal>O corpo deste ensaio irá mostrar que a descrição da mente de
um meditador em jhana, encontrada no próprio Cânone e mencionada no <i>Patisambhidamagga</i>
como um símile, envolvendo uma comparação da mente com uma nítida lua cheia,
degenera para a literalidade dessa imagem como um fenômeno visual produzido
internamente. Como não é fácil detalhar o conteúdo da mente, o Buda, freqüentemente,
empregava símiles comparando objetos visuais e outros objetos sensuais com o
conteúdo mental para que os meditadores pudessem entender com clareza o que
eles deveriam estar buscando e experimentando. Nas diversas tradições
religiosas, encontramos freqüentemente uma tendência ingênua para tomar
literalmente aquilo que foi intencionado como um símile. Parece que isso
ocorreu em alguma etapa do processo e acabou se tornando ‘santificado’ na
descrição do <i>patibhaganimitta</i> ou “sinal de contrapartida” no <i>Visuddhimagga</i>.
É importante que as novas gerações de meditadores ocidentais não sejam
induzidos em erro por este provável erro histórico.</p>

<p class=Normal>Os termos <i>nimitta</i> ou “sinal” e <i>patibhaganimitta</i>
ou “sinal de contrapartida” são freqüentemente mencionados<span
style="mso-spacerun: yes">  </span>neste ensaio, e é melhor esclarecer o seu
significado desde o início. O “sinal” significa uma marca característica ou
fenômeno que acompanha e ajuda a identificar uma certa experiência. Por
exemplo, a gripe é de modo freqüente acompanhada de fraqueza e náusea; nesse
caso, a náusea seria um sinal da gripe. Extrema felicidade pode ser acompanhada
por uma sensação de leveza do corpo e lágrimas; esses seriam sinais de
felicidade. Um médico busca certos sinais que de modo típico acompanham certas
enfermidades. Da mesma forma, certos sinais são característicos da entrada em
estados profundos de concentração correta e são intrínsecos aos estados de
jhana.<span style="mso-spacerun: yes">  </span></p>



<p class=Normal>De acordo com as definições (tomadas dos Comentários)
encontradas no Buddhist Dictionary do Ven. Nyanatiloka, existem três tipos de <i>nimitta</i>.
O primeiro tipo é<span style="mso-spacerun: yes">  </span><i>parikamma-nimitta</i>,
que se refere à percepção do objeto bem no início da concentração – este também
é conhecido como “imagem ou sinal preparatório’. Quando a mente alcança um grau
débil de concentração, surge uma imagem ou sinal ainda instável e não muito
nítido chamado <i>uggaha-nimitta</i> ou “sinal adquirido”. Essa percepção
antecede o surgimento de uma imagem totalmente clara e estável chamada <i>patibhaga-nimitta</i>
ou “imagem de contrapartida” ou “sinal de contrapartida”. O surgimento desse
terceiro tipo de <i>nimitta</i> indica o aparecimento da concentração de
acesso, o estado que antecede a plena absorção do jhana. Ambos os estados
compartem do mesmo sinal e diferem apenas na intensidade dos fatores que
acompanham cada estado. E como mencionado na definição, o sinal de
contrapartida é compreendido como uma versão mais refinada e nítida do sinal e
é o resultado natural da atenção e concentração intensificadas. Conhecendo
esses sinais, tanto o praticante como o mestre estarão aparelhados para avaliar
o êxito ou fracasso das respectivas realizações na prática de concentração. </p>

<p class=Normal><b>DISCUSSÃO</b></p>

<p class=Normal>A atenção plena na respiração, (<i>anapanasati</i>), é um
dos mais importantes objetos ou temas de meditação recomendados pelo Buda. E é
também um dos métodos de meditação mais populares empregados pelas gerações
atuais e passadas de praticantes do Budismo Theravada que buscam realizar o
caminho completo para a libertação. Esse método é descrito em vários suttas que
fazem parte do Cânone em Pali ( por exemplo o <a href="../sutta/MN118.php">MN
118</a>, <a href="../sutta/MN10.php">MN 10</a>, <a href="../sutta/DN22.php">DN 22</a>). No entanto, os suttas
são bastante concisos e em alguns casos contêm poucos detalhes ao tratar dos
métodos de meditação. Por conseguinte, encontramos que o principal objetivo da
exegese pós-canônica é comentar, explicar, complementar ou esclarecer os textos
do Cânone que possam ser considerados abstrusos ou carentes de informação. </p>



<p class=Normal>Com relação ao tema da meditação da respiração, três dos
comentários, o <i>Visuddhimagga</i> (Vis., 500 DC, 1a. edição em inglês em
1956), o <i>Vimuttimagga</i> (Vim., 100 DC ?, 1a. edição em inglês em 1961) e o
<i>Patisambhidamagga</i> (Pat., 300 AC ?, 1a. edição em inglês em 1982) estão no
momento disponíveis em traduções para o inglês. Tanto mestres como praticantes
empregam-nos amplamente como referências valiosas para esclarecer aspectos
chave da prática. Tradicionalmente, o <i>Visuddhimagga</i>, o trabalho mais
recente dos três acima mencionados, tem sido usado e é considerado o padrão
quiçá com mais autoridade a ser seguido como manual de meditação. </p>



<p class=Normal>No que diz respeito ao sinal, (<i>nimitta</i>), e ao sinal
de contrapartida, (<i>patibhaganimitta</i>), que surgem durante a meditação da
respiração, há discrepâncias significativas entre o <i>Visuddhimagga</i> e o <i>Vimuttimagga</i>.
Vários textos escritos por mestres contemporâneos mencionam as características
do sinal e sinal de contrapartida surgindo durante a meditação da
respiração.<span style="mso-spacerun: yes">  </span>Freqüentemente, essas
descrições tomam por base a clássica descrição do símile encontrada no <i>Visuddhimagga</i>,
talvez como uma medida cautelosa visando não se afastar da ortodoxia. No
entanto, como mostramos mais abaixo, essa descrição do sinal (adquirido ou
contrapartida) pode se revelar muito enganadora e, como amiúde declarado por
meditadores frustrados, obscura.</p>



<p class=Normal>No Visuddhimagga a descrição da atenção plena na respiração
(Vis.213-215, p.277), para. 213 diz o seguinte:</p>



<p class=Normal style='margin-left:.75in'>...Assim também, o bhikkhu não deveria
procurar a inspiração e a expiração em nenhum outro lugar a não ser aquele
normalmente tocado por elas. Ele deveria tomar a atenção plena como corda e o
entendimento como aguilhada e fixar a mente no lugar normalmente tocado pela
inspiração e expiração e prosseguir empenhando sua atenção nisso. Pois, agindo
deste modo, a inspiração e a expiração reaparecerão depois de não muito
tempo... E assim, ele poderá prendê-las com a corda da atenção plena, e,
emparelhando-as no mesmo lugar e estimulando-as com a aguilhada do
entendimento, ele se manterá empenhado no objeto de meditação. </p>



<p class=Normal style='margin-left:.75in'>214. Assim procedendo, o sinal [veja a nota
correspondente no próximo parágrafo] em breve aparecerá para ele. Mas não é o
mesmo sinal para todos; ao contrário, alguns dizem que quando ele aparece,
assim o faz<span style="mso-spacerun: yes">  </span>produzindo um toque suave
como o do algodão ou da seda, ou como uma brisa suave.</p>



<p class=Normal style='margin-left:.75in'>215. Mas esta é a explicação dada
nos comentários: o sinal aparece para alguns como uma estrela ou um punhado de
pedras preciosas, ou como um punhado de pérolas, para outros como um toque
áspero parecido com o das sementes de algodão ou de um pino feito de madeira,
para outros como o de uma longa trança de fios ou de uma coroa de flores, ou
uma <u>baforada de fumaça</u>, para outros como uma <u>teia de aranha</u>
esticada ou como uma camada muito fina de <u>nuvem,</u> ou como uma flor de
lótus, ou como uma roda de carruagem, ou como o <u>disco lunar,</u> ou como o <u>disco
solar</u>. [sublinhado pelo autor]</p>

<p class=Normal>Uma nota que consta do comentário ao <i>Visuddhimagga</i>
diz o seguinte:</p>



<p class=Normal style='margin-left:.75in'>“O sinal” é o sinal adquirido e o
sinal de contrapartida, pois ambos são aqui mencionados juntos. Os três
símiles, começando com o do algodão, se referem ao sinal adquirido e os
restantes a ambos. “Alguns” são certos mestres. Os símiles, a partir do
“punhado de pedras preciosas”, se referem ao sinal de contrapartida.
(Paramattha-manjusa, Visuddhimagga Atthakatha; Vism. p.786, n.58).</p>



<p class=Normal>Os símiles que constam das seções anteriores (Vis.214-215)
representam tanto percepções tácteis como visuais. Os trechos a seguir são apresentados
para que o leitor avalie se ocorreu alguma confusão resultante de erro na
transmissão, (talvez um erro na transcrição), baseada em dados obtidos dos
comentários mais antigos, tais como do <i>Vimuttimagga</i> e do texto canônico <i>Patisambhidamagga</i>,
ou por que foi tomado literalmente o que originalmente pretendia ser um símile.</p>



<p class=Normal>O <i>Vimuttimagga</i> (p.68), ao mencionar o “discernimento
das qualidades” dos diversos temas de meditação, afirma que “ ... um objeto de
meditação agarra o sinal através do contato. A saber, a atenção plena na
respiração. E novamente, um objeto de meditação agarra o sinal através da visão
ou contato. A saber, a kasina do ar.” Esta distinção é crítica. Ela mostra que
a meditação da respiração é distinta dos demais objetos de concentração por ser
exclusivamente táctil.</p>



<p class=Normal>Os objetos visuais podem ser percebidos por alguns
meditadores durante a meditação da respiração como um efeito colateral, no
entanto, o meditador deve permanecer focado exclusivamente na sensação táctil
da respiração. Portanto, como mencionado no <i>Patisambhidamagga</i> (170.,
p.172):</p>



<p class=Normal style='margin-left:.75in'>... o bhikkhu senta, estabelecendo
a atenção plena na ponta do nariz ou no lábio, sem acompanhar a inspiração e a
expiração enquanto estas se aproximam e se afastam, ... o corpo e a cognição
daquele que é energético [nesta tarefa] se torna maleável, ... os seus
pensamentos aplicados silenciam .... [e] as suas tendências subjacentes são
suprimidas ...</p>



<p class=Normal>Voltando aos símiles do <i>Visuddhimagga</i>, mencionados
acima (Vis.214-215), e comparando-os com aquilo que é encontrado nas descrições
correspondentes no <i>Vimuttimagga,</i> (Atenção Plena na Respiração,
pp.158-159; veja o parágrafo abaixo), é possível observar que eles são
diametralmente opostos no significado aparentemente pretendido. Enquanto os
símiles do <i>Visuddhimagga</i> são formulados referindo-se àquilo que poderá
ser encontrado como sinal ao qual se deve dar atenção, no <i>Vimuttimagga</i>
encontramos palavras de advertência para que nos abstenhamos de dar atenção a
essas percepções, (ao invés de dar atenção ao sinal táctil da respiração). O
trecho correspondente do <i>Vimuttimagga</i> está reproduzido na íntegra a
seguir:</p>



<p class=Normal style='margin-left:.75in'>Para o iogue que se ocupa com a respiração
com a mente purificada das nove contaminações, a imagem surge com uma sensação
prazerosa semelhante àquela que é produzida na ação de fiar algodão ou seda.
Também é comparada à sensação prazerosa produzida por uma brisa. Assim, ao
inspirar e expirar o ar toca no nariz ou no lábio e causa o estabelecimento da
atenção plena na percepção do ar. Isto não depende de cor ou forma e é chamado
de imagem. Se o iogue desenvolver a imagem, [sinal], e aumentá-la na ponta do
nariz, entre as sobrancelhas ou na testa, ou estabelecê-la em vários lugares,
ele irá sentir como se a sua cabeça estivesse cheia de ar. Incrementando a
imagem desse modo, todo o seu corpo irá se permear com o prazer. Isso é chamado
de perfeição. </p>

<p class=Normal style='margin-left:.75in'>Novamente, para um outro iogue: ele
vê várias imagens desde o princípio. Ele vê várias formas, tais como <u>fumaça</u>,
<u>névoa</u>, <u>poeira</u>, <u>ouro pulverizado</u> ou ele experimenta algo
semelhante à picada de uma agulha ou à mordida de uma formiga. Se a sua mente
não tiver um claro entendimento dessas diferentes imagens, ele ficará
confuso[!]. E assim ele obterá algo oposto à percepção da respiração. Se a sua
mente estiver purificada, o iogue não experimentará a confusão. Ele se ocupará
com a respiração e <u><span style="mso-spacerun: yes"> </span>não causará o
surgimento de outras percepções</u> [sublinhado pelo autor]. Meditando desse
modo ele será capaz de dar um fim à confusão e obter a imagem, [sinal], sutil.
Ele estará atento à respiração com a mente livre. E a imagem, [sinal], estará
livre. E pelo fato da imagem, [sinal], estar livre, o desejo surgirá. Com o
desejo livre, este iogue estará atento à respiração com equanimidade. Com a
equanimidade, o desejo e o prazer livres, ele estará atento à respiração e a
sua mente não será perturbada. Se a sua mente não estiver perturbada, ele
destruirá os obstáculos e estimulará o surgimento dos fatores de jhana. Assim,
este iogue alcançará o calmo e sublime quarto jhana. É dessa forma ampla que
foi ensinado acima. </p>



<p class=Normal>O alerta para não ficar distraído pode ter sido obtido
diretamente do discurso da Atenção Plena na Respiração (<a
href="../sutta/MN118.php#T26">Anapanasati Sutta MN 118.26</a>): “<span
lang=PT-BR style='mso-ansi-language:PT-BR'>Eu não digo que possa existir
desenvolvimento da atenção plena para aquele que é esquecido e desprovido de plena consciência.”</span></p>



<p class=Normal>A frase “sensação prazerosa semelhante àquela que é
produzida na ação de fiar algodão ou seda” deve ser compreendida como a sensação
táctil prazerosa experimentada num certo ponto da mão pelo fiador que segura e
direciona e ao mesmo tempo fia um fio de algodão. A interpretação do símile
dessa forma é apropriada no sentido de que o contato inicial com o fio é
sentido como áspero e depois de algum tempo muda a sua característica
(dormência, pressão, calor, etc.) para uma qualidade de percepção distinta pelo
efeito da fricção contínua. Este é um símile mais refinado do que aquele
encontrado no <i>Visudhimagga</i>, que se contenta com a imagem estática do
“toque do algodão.”</p>



<p class=Normal>A frase “isto não depende de cor ou forma” deixa bem claro
que o meditador não deve esperar que o sinal da atenção plena na respiração
seja uma imagem visual, visto que não é possível conceber uma percepção visual
que não tenha cor e forma. O que pode ser inferido dessa frase é que o sinal é
uma percepção táctil. A propósito, no <i>Patisambhidamagga</i>, o tratado sobre
a respiração mais antigo e mais abrangente, não há nenhuma menção ao longo de
toda a seção sobre a meditação da respiração de um <i>nimitta</i> visual ou de
uma “luz”. </p>



<p class=Normal>Um grande mistério é solucionado quando compreendemos que a
maioria das imagens atribuídas ao sinal de contrapartida no <i>Visuddhimagga</i>
e às “distrações “ no <i>Vimuttimagga,</i> são encontradas no mais antigo <i>Patisambhidamagga,</i>
como parte de uma descrição metafórica de um bhikkhu liberto das impurezas, por
conta da sua distinção na prática da meditação da respiração. As descrições
seguem abaixo:</p>



<p class=Normal style='margin-left:.75in'>Aquele cuja atenção plena na
respiração é perfeita, bem desenvolvida e gradualmente conduzida à sua
maturação de acordo com o que foi ensinado pelo Buda, é que ilumina o mundo tal
qual a lua cheia livre de nuvens (Pat.III, 171, p.172). E,</p>



<p class=Normal style='margin-left:.75in'>Tal qual a <u>lua cheia</u> livre
de nuvens: as impurezas são como as nuvens, o conhecimento dos nobres é como a
lua, o bhikkhu é como o filho da divindade que possui a lua cheia. Tal qual a
lua que quando liberta das <u>nuvens</u>, liberta da <u>névoa</u>, liberta da <u>fumaça</u>
e da <u>poeira</u>, liberta das garras do Demônio do Eclipse Rahu, <u>brilha</u>,
<u>cintila</u> e <u>resplandece</u>, assim também o bhikkhu que está liberto de
todas as impurezas brilha, cintila e resplandece. Por conseguinte, foi dito
“Tal qual a lua cheia livre de nuvens” (Pat.III, 182, p.175). [sublinhado pelo
autor]</p>



<p class=Normal>Aqui, o que num texto do Cânone é dado como um símile para a
mente, no <i>Vimuttimagga</i> é tomado literalmente, ainda que corretamente,
como percepções visuais, como imagens às quais não se deve dar atenção. O <i>Visuddhimagga</i>,
no entanto, se equivoca duas vezes tomando os símiles “fumaça”, “névoa”, “poeira”,
“brilho”, “cintilante”, “resplandecente”, e “lua” como imagens visuais no
sentido literal, e também ao caracterizá-los como sinal de contrapartida, um
indicador do sucesso [!], em oposição direta ao <i>Vimuttimagga</i>.</p>



<p class=Normal>Resta-nos somente perguntar como essas imagens metafóricas,
encontradas no final do capítulo, que descreve a meditação da respiração no <i>Patisambhidamagga</i>,<span
style="mso-spacerun: yes">  </span>acabaram por fim se convertendo em eventos
visuais literais relacionados à prática de meditação nos comentários posteriores.
Dada a evidência apresentada neste ensaio, é aconselhável considerar ambos, o <i>Vimuttimagga</i>
e o <i>Patisambhidamagga,</i> como textos mais confiáveis no que diz respeito à
meditação da respiração.</p>



<p class=Normal>Somente no <i>Patisambhidamagga</i> o material é tratado de modo
apropriado. Os símiles para a qualidade da mente, como “clara”, “iluminada” ou
“livre de nuvens”, são tratados como símiles, e além disso as imagens dos
símiles “nuvens”, “névoa”, etc., são entendidas de forma correta como
impedimentos para aquela claridade. O editor,<span style="mso-spacerun: yes"> 
</span>(de acordo com a tradição Acariya Buddhaghosa), do <i>Visuddhimagga</i>
demostra na verdade um certo desconforto com a “diversidade de percepções” dos
vários <i>nimittas</i> para a meditação da respiração e demonstra esse
desconforto explicando que tal diversidade se origina da mera diferença de
percepção entre os meditadores, (veja a citação no próximo parágrafo). Nem essa
explicação e tampouco a necessidade de formulá-la aparecem nos comentários mais
antigos.</p>



<p class=Normal style='margin-left:.75in'>216. Na verdade, isso se assemelha
a uma ocasião em que muitos bhikkhus estão sentados juntos recitando um sutta e
um bhikkhu pergunta, “Com o que se parece este sutta para você?”, um diz, “Para
mim ele se parece com uma grande enxurrada montanha abaixo,” outro diz,“Para
mim, com um grupo de árvores na floresta,” ainda um outro diz, “Para mim, ele é
como a sombra fresca de uma frondosa árvore frutífera.” Pois o mesmo sutta tem
uma aparência distinta para cada um devido à diferença de percepção de cada um.
Assim também, este único objeto de meditação<span style="mso-spacerun: yes"> 
</span>aparece de modo diferente devido à diferença entre as percepções. Essa
diferença é nascida da percepção, a sua fonte é a percepção, ela é produzida
pela percepção. Por conseguinte, deve ser compreendido que quando a aparência é
distinta, é devido à diferença entre as percepções. (Vis. VIII, 216, p.278).</p>



<p class=Normal>Tenho certeza que muitos meditadores já se perguntaram
porque o Buda<span style="mso-spacerun: yes">  </span>deixou de mencionar a
informação essencial acerca do “sinal” e do “sinal de contrapartida” na
meditação da respiração, que o <i>Visuddhimagga</i> considera ser tão essencial
para o êxito da prática dos jhanas. Espero que este ensaio demostre que a
descrição do Buda para a prática da meditação da respiração contém toda a
informação necessária e suficiente para o sucesso dessa prática. </p>



<p class=Normal>Eu adicionaria que o único sinal de jhana confiável, e que
se aplica a todos os casos, é a descrição dos fatores de jhana dada pelo
próprio Buda, quer o objeto de meditação seja visual ou táctil. Além disso,
espero que o meditador compreenda que a clareza e o refinamento progressivo da
sua percepção do objeto de meditação são simplesmente os “efeitos colaterais”
da clareza e luminosidade da mente tranqüila e focada. </p>



<p class=Normal>Por fim, gostaria de enfatizar que o objeto da meditação da
respiração é o contato com o ar. A qualidade do elemento ar é de importância
crítica nesta meditação. Se o Buda estivesse interessado na mera sensação
proveniente do contato, então seria mais simples tocar o nariz com os dedos.
Tomar a leveza do ar como uma experiência corporal é crítico. Como o <i>Vimuttimagga
</i>diz:</p>



<p class=Normal style='margin-left:.75in'>Ele irá sentir como se a sua cabeça
estivesse cheia de ar. Com o aumento dessa sensação, o seu corpo irá se permear
com o prazer. Isso é chamado de perfeição. [mencionado acima].</p>

<p class=Normal><b>LOCALIZAÇÃO, LOCALIZAÇÃO, LOCALIZAÇÃO: UM PEQUENO ASSUNTO PERTINENTE</b></p>

<p class=Normal>Um assunto secundário, pertinente, em relação ao <i>nimitta</i>
da respiração é, uma vez mais, um mal entendido visível que evoluiu para a sua
forma final no <i>Visuddhimagga</i>. </p>



<p class=Normal>Esta frase crítica é empregada no <a href="../sutta/MN10.php">Satipatthana
Sutta</a> e no <a href="../sutta/MN118.php">Anapanasati Sutta</a>: “<i>parimukham
satim upatthapetva</i>,” que tem sido traduzida como “<span lang=PT-BR
style='mso-ansi-language:PT-BR'>estabelece a atenção plena à sua frente</span>.”
Ficamos nos perguntando porque o <i>Patisambhidamagga</i>, o <i>Vimuttimagga</i>
e o <i>Visudhimagga</i> todos com muita segurança dão a localização do contato
da respiração nas narinas. Além disso, encontramos nessas três obras: “quer
seja no nariz ou no lábio.” E nesse caso, Buddhaghosa, o editor, dá uma
explicação que “uma pessoa com a nariz comprido sente a respiração nas narinas
à medida que o ar passa pelo nariz. Uma pessoa com um nariz pequeno no entanto,
sente o ar no lábio superior.” Isso também soa estranho se pensarmos um pouco a
respeito, porque se uma pessoa tiver um “nariz pequeno”, ela será capaz de
sentir somente a exalação de ar quente saindo das narinas para o lábio
superior. Toda a inspiração terá sido perdida. Portanto, parece que temos mais
uma charada.</p>



<p class=Normal>Se olharmos para a palavra “<i>mukha</i>”, no sutta original,
significa, literalmente, “entrada” ou “boca.” Se aplicarmos este significado
óbvio teremos: “Ele <span lang=PT-BR style='mso-ansi-language:PT-BR'>estabelece
a atenção plena na entrada”, sendo que a entrada é ou o nariz ou a boca. Os
antigos comentaristas estão pressupondo que o leitor entende que o meditador
poderá estar respirando ou pela boca ou pelo nariz. Se ele estiver respirando
através da boca deverá dirigir a atenção para o contato do ar com o lábio. Na
verdade, é um conselho bastante sensato, pois seria uma lástima ter que
abandonar a meditação da respiração só por causa de um resfriado ou porque o
nariz está entupido! Portanto, podemos ver que aquilo que começou como uma
descomplicada localização do contato com a respiração no nariz ou na boca, isto
é, “na entrada”, pouco a pouco assumiu a confusa adição de “uma pessoa com o
nariz comprido ou pequeno.” O debate sobre o significado desta frase começou
muito cedo (veja abaixo a citação da nota original no </span><i>Patisambhidamagga</i>)
e na verdade todos os três comentários optaram por <i>mukha</i> como sendo
nariz ou boca.</p>



<p class=Normal style='margin-left:.75in'>“Tem o sentido de abraçar”, está
com o sentido de ser abraçado. O que é abraçado? A saída. Qual saída? A
concentração baseada na atenção plena na respiração é em si mesma a saída
direta até o caminho do arahant. Daí, a frase “tem o sentido de saída.” O
significado de “saída do ciclo de renascimentos” é expresso através do
significado da palavra <i>mukha,</i> (boca), como principal, (frente). “Tem o
sentido de fundação”, está com o sentido de essência individual. O significado
expresso por todas essas palavras é: fez da atenção plena uma saída abraçada.
Mas alguns dizem que “tem o sentido de abraçar” significa “abraçar com o
sentido de atenção plena,” e que “tem o sentido de saída” significa “porta de
entrada e saída significando a inspiração e a expiração.” Então, o significado
pretendido é: Estabeleceu a atenção plena como a saída abraçada das inspirações
e expirações. (Nota 14, Edição em Inglês; PsA 350-1) </p>



<p class=Normal>Alguns mestres modernos têm sugerido que não importa onde o
contato com a respiração esteja localizado, provavelmente em resposta à frase
que ocorre mais tarde no sutta: “<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Eu inspiro experienciando todo o corpo ...”, etc. E visto que o corpo da
respiração não está mencionado de forma explícita, eles sentem que há espaço
para interpretação. Mas a respiração como um “corpo” é mencionada de modo
explícito no </span>Anapanasati Sutta, embora não no Satipatthana Sutta, e o
significado da frase é o mesmo: “<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Eu digo que esse é um<span style="mso-spacerun: yes">  </span>corpo
entre os corpos, ou seja, a inspiração e a expiração” (</span><a
href="../sutta/MN118.php#T24">Anapanasati Sutta, MN 118.24</a>; a nota diz que
“<span lang=PT-BR style='mso-ansi-language:PT-BR'>Inspiração e expiração devem
ser consideradas ... entre os fenômenos corporais uma vez que o objeto da
atenção é a sensação do toque da respiração entrando e saindo das narinas”). Há
também uma localização explícita da “entrada” neste sutta, com o que os três
comentários estão de acordo, </span>qualquer que seja a confusão que tenha
surgido mais tarde. Ela também ignora o símile que vem em seguida à localização
explícita, isto é, “<span lang=PT-BR style='mso-ansi-language:PT-BR'>Da mesma
forma como um torneiro habilidoso ou seu aprendiz, quando faz uma volta longa,
compreende: ‘Eu faço uma volta longa’; ou, quando faz uma volta curta,
compreende: ‘Eu faço uma volta curta’; assim também, inspirando longo, um
Bhikkhu compreende: ‘Eu inspiro longo’...ele treina dessa forma: ‘Eu expiro tranqüilizando a formação do corpo.’” </span>(<a
href="../sutta/MN10.php#T4">Satipatthana Sutta, MN10.4</a>). O Buda incluiu
este símile aparentemente redundante por uma razão. Símiles são como retratos,
valem por mil palavras e em geral sobrevivem à carnificina das traduções. Este
é o mecanismo de segurança do Buda para mostrar que assim como um torneiro fixa
a sua atenção num só ponto com o cinzel enquanto o torno está em constante
movimento, o meditador faz o mesmo no “ponto de entrada” enquanto a respiração
segue fluindo continuamente. Basicamente, todos os comentários lograram
preservar essa noção do “símile da serra”, mas infelizmente a boca como
localização foi descuidada na época do <i>Visuddhimagga</i>.</p>



<p class=Normal>Tudo isso não quer dizer que existe apenas uma forma de
alcançar a tranqüilidade usando a respiração. Se alguém desenvolveu uma técnica
que resulta em jhana, e que não segue as instruções explícitas, está bem.<span
style="mso-spacerun: yes">  </span>O que quer que funcione vale. </p>

<p class=Normal><b>RESUMO</span></b></p>

<p class=Normal>A seguir, um breve resumo de instruções para os meditadores
que praticam a meditação da atenção plena na respiração:</p>



<ul style='margin-top:0in' type=disc>
 <li class=Normal style='mso-list:l0 level1 lfo3;tab-stops:list .5in'>Coloque
     a atenção na sensação da respiração/ar onde quer que ela entre e saia do
     corpo. </li>
 <li class=Normal style='mso-list:l0 level1 lfo3;tab-stops:list .5in'>Se
     surgirem percepções visuais, ignore-as.</li>
 <li class=Normal style='mso-list:l0 level1 lfo3;tab-stops:list .5in'>Não
     permita que a mente vagueie. Regresse para o ponto de contato da
     respiração. </li>
 <li class=Normal style='mso-list:l0 level1 lfo3;tab-stops:list .5in'>Mantenha
     a atenção naquele ponto ao longo de toda a duração da inspiração e
     expiração.<span style="mso-spacerun: yes">  </span></li>
 <li class=Normal style='mso-list:l0 level1 lfo3;tab-stops:list .5in'>A
     sensação, ou percepção, do ar se movendo irá mudar para uma sensação
     estática, esse é um sinal da mente se acalmando. </li>
 <li class=Normal style='mso-list:l0 level1 lfo3;tab-stops:list .5in'>Permaneça
     com essa qualidade aérea, flutuante, que deve permear toda a cabeça.
     Experimente um frescor e um vazio aéreo na cabeça. Essa percepção poderá
     se estender por todo o corpo. Este é mais um sinal de crescente tranqüilidade.<span
     style="mso-spacerun: yes">  </span></li>
 <li class=Normal style='mso-list:l0 level1 lfo3;tab-stops:list .5in'>Permaneça
     com essa leveza aérea focando nessa experiência.</li>
 <li class=Normal style='mso-list:l0 level1 lfo3;tab-stops:list .5in'>Todos
     os obstáculos deverão ter desaparecido e os cinco fatores de jhana estarão
     presentes num grau que poderá ser fraco, médio ou forte.<span
     style="mso-spacerun: yes">  </span></li>
 <li class=Normal style='mso-list:l0 level1 lfo3;tab-stops:list .5in'>Use o <a
     href="../sutta/MN118.php">Anapanasati Sutta</a> para mais instruções. </li>
</ul>



<p class=Normal>Eu espero que os pontos acima ajudem a esclarecer as
possíveis confusões enfrentadas pelos meditadores e que eles possam respirar
aliviados à medida que progredirem ao longo do caminho. </p>


