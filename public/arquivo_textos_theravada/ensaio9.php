<p class=Normal>Nesta edição do boletim
periódico estamos combinando a coluna principal com a coluna 'Estudos de
Suttas' para revermos um discurso do Buda que freqüentemente é mencionado, o
Kalama Sutta. O discurso tem sido descrito como o 'alvará do Buda para o livre
inquérito', e embora o discurso, com certeza, contraponha os ditames do
dogmatismo e da fé cega com um vigoroso chamado para a livre investigação, é
questionável se ele pode dar suporte a todas as idéias que lhe têm sido
atribuídas. Com base em um único trecho, mencionado fora do seu contexto, o
Buda é convertido num empirista pragmático que descarta toda doutrina e fé e
cujo Dhamma é uma simples ferramenta para atingir a verdade dos livres
pensadores,<span style='mso-spacerun:yes'>  </span>convidando cada um a aceitar
e rejeitar qualquer coisa que queira. <o:p></o:p></span></p>

<p class=Normal>Mas o Kalama Sutta
realmente justifica tais idéias? Ou encontramos nessas afirmações apenas mais
uma variação censurável dessa velha tendência de interpretar o Dhamma de acordo
com<span style='mso-spacerun:yes'>  </span>noções simpáticas a cada um - ou
àqueles para os quais se está pregando? Vamos rever o Kalama Sutta com extremo
cuidado, dentro dos limites do espaço designado para este ensaio, lembrando que
para entender os pronunciamentos do Buda corretamente é essencial tomar em
conta as suas intenções ao fazê-los. <o:p></o:p></span></p>

<p class=Normal>O trecho que tem sido
mencionado com tanta freqüência diz o seguinte: &quot;Claro que vocês estão
confusos Kalamas. Claro que vocês têm dúvidas. Porque a dúvida surge com
relação a qualquer assunto que cause a perplexidade. Dessa forma, Kalamas,
nesse caso não se deixem levar pelos relatos, pelas tradições, pelos rumores,
por aquilo que está nas escrituras, pela razão, pela inferência,
pela analogia, pela competência (ou confiabilidade) de alguém, por respeito por alguém, ou pelo pensamento, ‘Este contemplativo é o nosso mestre.’ Quando
vocês sabem por vocês mesmos que, ‘Essas qualidades são inábeis; essas
qualidades são culpáveis; essas qualidades são passíveis de crítica pelos
sábios; essas qualidades quando postas em prática conduzem ao mal e ao
sofrimento’ - então vocês devem abandoná-las…. Quando vocês sabem por vocês
mesmos que, ‘Essas qualidades são hábeis; essas qualidades são isentas de
culpa; essas qualidades são elogiadas pelos sábios; essas qualidades quando
postas em prática conduzem ao bem-estar e à felicidade” - então vocês devem
penetrar e permanecer nelas.&quot;<o:p></o:p></span></p>

<p class=Normal>Agora esse trecho, tal como
tudo aquilo que foi dito pelo Buda, foi expresso dentro de um contexto específico
- com uma audiência e situação particularres em mente - e portanto precisa ser
entendido dentro desse contexto. Os Kalamas, cidadãos da cidade de Kesaputta,
haviam recebido a visita de vários mestres religiosos com opiniões divergentes,
cada um propondo as suas próprias doutrinas e destruindo as doutrinas dos seus
predecessores. Isso deixou os Kalamas perplexos e assim quando o
&quot;contemplativo Gotama&quot;, conhecido por ser um Iluminado, chegou na
cidade, eles o abordaram na esperança de que ele pudesse ser capaz de
esclarecer a confusão. Pelo desenvolvimento subseqüente do sutta, fica claro
que as questões que os deixavam perplexos era a realidade do renascimento e a
retribuição cármica por ações boas e más. <o:p></o:p></span></p>

<p class=Normal>O Buda começa assegurando
aos Kalamas que sob tais circunstâncias é perfeitamente justo que eles tenham
dúvida, uma garantia que encoraja o livre inquérito. Em seguida ele pronuncia o
trecho acima, aconselhando os Kalamas a abandonar aquelas coisas que eles sabem
por si mesmos serem ruins e de realizar aquelas coisas que eles sabem por si
mesmos serem boas. Esse conselho pode ser perigoso se dado para aqueles cuja
noção ética é rudimentar e portanto podemos assumir que o Buda considerava os
Kalamas como pessoas de sensibilidade moral refinada. Em todo caso, ele não os
deixou inteiramente por conta própria, pois, ao questioná-los, os levou a ver
que o desejo, a raiva e a delusão conduzem ao prejuízo e sofrimento próprio e
dos outros e devem ser abandonados e os seus opostos, sendo benéficos a todos,
devem ser desenvolvidos. <o:p></o:p></span></p>

<p class=Normal>A seguir o Buda explica que
&quot; um nobre discípulo despojado de cobiça, despojado de má vontade, não
deludido&quot; permanece permeando o mundo com amor bondade, compaixão, alegria
altruista e equanimidade ilimitadas. Assim purificado da raiva e maldade, ele
desfruta aqui e agora de quatro &quot;garantias&quot;. Se existe uma vida
futura e resultados cármicos, então ele irá ter um renascimento agradável,
enquanto que se não existir, ainda assim ele irá viver feliz aqui e agora; se
resultados ruins recaem sobre quem pratica o mal, então nenhum mal lhe irá
recair, e se resultados ruins não recaírem sobre quem pratica o mal, então de
todos modos ele estará purificado. Com base nisso os Kalamas expressam a sua
apreciação pelo discurso do Buda e buscam refúgio na Jóia Tríplice. <o:p></o:p></span></p>

<p class=Normal>Assim, o Kalama Sutta
sugere, tal como com freqüência se interpreta, que um discípulo do caminho
Budista pode deixar de lado toda a fé e doutrina, que ele deve fazer da sua
própria experiência pessoal o critério para julgar as afirmações do Buda e para
rejeitar aquilo que não se enquadra nela? É verdade que o Buda não pede que os
Kalamas aceitem o que ele diz por confiança nele, mas notemos um ponto
importante: os Kalamas, no início do discurso, não eram discípulos do Buda.
Eles o abordaram apenas como um conselheiro que poderia ajudar a dissipar as
suas dúvidas, mas eles não se dirigiram a ele como o Tathagata, o descobridor
da Verdade, que poderia lhes ensinar o caminho para o progresso espiritual e a
libertação final. <o:p></o:p></span></p>

<p class=Normal>Dessa forma, como os
Kalamas ainda não haviam aceitado o Buda, no que se relaciona à sua missão
única, como o revelador da verdade libertadora, não seria adequado que ele lhes
expusesse o Dhamma singular da sua própria Revelação: ensinamentos tais como as
Quatro Nobres Verdades, as três características e os métodos de meditação
baseados nelas. Esses ensinamentos são especificamente destinados àqueles que
aceitaram o Buda como seu guia para a libertação e nos suttas ele os expõem
somente para aqueles que &quot;tenham adquirido fé no Tathagata&quot; e que
possuem a percepção necessária para compreendê-los e aplicá-los. Os Kalamas, no
entanto, no início do discurso ainda não representam um solo fértil para que
ele dissemine as sementes da sua mensagem libertadora. Ainda confundidos pelas
afirmações conflituosas às quais foram expostos, eles ainda não possuem clareza
nem acerca dos fundamentos da virtude. <o:p></o:p></span></p>

<p class=Normal>Apesar disso, após
aconselhar os Kalamas a não confiar na tradição estabelecida, raciocínio
abstrato e gurus carismáticos, o Buda lhes propõe um ensinamento que pode ser
verificado de imediato e que é capaz de estabelecer um fundamento sólido para
uma vida de disciplina moral e purificação mental. Ele mostra que quer exista
ou não uma vida após a morte, uma vida com autocontrole moral e com amor e
compaixão por todos os seres traz as suas próprias recompensas intrínsecas aqui
e agora, uma felicidade e sentimento de segurança interior muito superior aos
prazeres frágeis que podem ser conquistados violando os princípios morais e se
entregando aos desejos da mente. Para aqueles que não estão preocupados em
olhar mais além, que não estão preparados para adotar convicções sobre uma vida
futura e mundos além do presente, tal ensinamento irá assegurar o seu bem estar
no presente e uma passagem segura para um renascimento agradável - contanto que
eles não regridam ao entendimento incorreto de negar uma vida após a morte e a
causação cármica. <o:p></o:p></span></p>

<p class=Normal>No entanto, para aqueles
cuja visão é capaz de ser ampliada para englobar os horizontes mais amplos da
nossa existência, esse ensinamento dado aos Kalamas aponta para além das suas
implicações imediatas, para o núcleo do Dhamma. Pois os três estados
apresentados pelo Buda para exame - desejo, raiva e delusão - não são apenas
motivo para conduta incorreta ou manchas morais na mente. Dentro da estrutura
dos seus ensinamentos eles são as contaminações básicas - as causas primárias
para o cativeiro e sofrimento - e toda a prática do Dhamma pode ser vista como
a tarefa para erradicar essas raízes maléficas através do desenvolvimento, até
a perfeição, dos seus antídotos - desapego, bondade e sabedoria. <o:p></o:p></span></p>

<p class=Normal>Assim, o discurso para os
Kalamas oferece uma prova inteligente para conquistar a confiança no Dhamma,
como uma doutrina viável de libertação. Começamos com um ensinamento cuja
validade pode ser confirmada de imediato por qualquer um com integridade moral
para segui-lo até as suas conclusões, ou seja, que as contaminações causam dano
e sofrimento tanto pessoal como social, que a sua remoção traz paz e felicidade
e que as práticas ensinadas pelo Buda são meios eficazes para alcançar a sua
remoção. Testando pessoalmente esse ensinamento, tendo como colateral apenas
uma fé temporária no Buda, a pessoa irá por fim chegar a uma crença mais firme,
fundamentada na experiência do poder libertador e purificador do Dhamma. Essa
maior certeza, com relação aos ensinamentos, traz consigo um aprofundamento da
fé no Buda como um mestre e dessa forma predispõe a pessoa a aceitar, com base
na confiança, aqueles princípios enunciados por ele que são relevantes na busca
pela iluminação, mesmo quando estes se encontram além da capacidade de
verificação da pessoa. Isto, na verdade, é um sinal da obtenção do entendimento
correto, no seu papel preliminar como precursor de todo o Nobre Caminho
Óctuplo. <o:p></o:p></span></p>

<p class=Normal>Em parte como reação ao
dogmatismo religioso, em parte como servilismo ao paradigma dominante do
conhecimento científico objetivo, tem se tornado moda pensar, apelando ao
Kalama Sutta, que os ensinamentos do Buda prescindem da fé e de uma doutrina
formulada e que pedem que aceitemos somente aquilo que possa ser verificado
pessoalmente. Essa interpretação do sutta, no entanto, esquece que o conselho
dado pelo Buda aos Kalamas estava sujeito ao entendimento de que eles não
estavam ainda preparados para ter fé nele e na sua doutrina; também esquece que
o sutta omite, por essa mesma razão, qualquer menção ao entendimento correto e
à<span style='mso-spacerun:yes'>  </span>perspectiva completa que se abre
quando se obtém o entendimento correto. Ao invés disso, ele oferece o conselho
mais sábio possível sobre como viver de maneira saudável e benéfica quando as
questões sobre as crenças mais significantes são deixadas de lado. <o:p></o:p></span></p>

<p class=Normal>O que pode ser defendido
com justiça é que aqueles aspectos dos ensinamentos do Buda que estão dentro do
alcance da nossa experiência comum podem ser pessoalmente confirmados com a
experiência e de que essa confirmação provê uma base sólida para a fé naqueles
aspectos dos ensinamentos que necessariamente transcendem a experiência comum.
A fé nos ensinamentos do Buda nunca é considerada como um fim em si mesma, nem
como garantia suficiente de libertação, mas somente como o ponto inicial para
um processo evolutivo de transformação interior que alcança a sua realização
com o insight pessoal. Mas para que esse insight exerça uma função
verdadeiramente libertadora, ele tem que ser desenvolvido dentro do contexto da
compreensão precisa das verdades essenciais que dizem respeito à nossa situação
no mundo e esfera de ação, no qual a libertação deve ser buscada. Essas verdades
nos foram transmitidas pelo Buda à partir da sua própria profunda compreensão
da condição humana. Aceitá-las com confiança após consideração cuidadosa é
iniciar uma jornada que transformará a fé em sabedoria, a confiança em certeza
e que culminará na libertação do sofrimento. <o:p></o:p></span></p>

