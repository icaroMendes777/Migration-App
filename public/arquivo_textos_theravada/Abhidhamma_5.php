<p class=Tit1 align=center style='text-align:center'>V. Compêndio da Condicionalidade<br> <i>(Paccayasangahavibhaga)</i></Tit1>


<p class=Normal>&nbsp;</p>

<hr size=2 width="100%" align=center>

<p class=Normal>&nbsp;</p>






<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Depois de
haver explicado os quatro tipos de realidade última e as suas categorias, o
Abhidhammatha Sangaha aborda no Compêndio da Condicionalidade a análise das
relações entre esses elementos em termos de <i>estados condicionadores</i>, <i>(paccayadhamma),</i>
e <i>estados condicionados</i>, <i>(paccayauppannadhamma), </i>vinculados por
meio de <i>forças condicionadoras</i>, <i>(paccayasatti). <o:p></o:p></i></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os
estados condicionados: </span></b><span lang=PT-BR style='mso-ansi-language:
PT-BR'>os estados condicionados são os fenômenos, <i>(dhamma), </i>que surgem
na dependência de condições, isto é, todas as <i>cittas</i>, todos os <i>cetasikas</i>
e todos os fenômenos materiais. <o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os
estados que são a sua condição</span></b><span lang=PT-BR style='mso-ansi-language:
PT-BR'>: Uma condição é um estado que tem eficácia no surgimento ou
persistência de outros estados. Isso significa que uma condição, quando estiver
operante, irá causar o surgimento de outros estados a ela conectados se ainda não
tiverem surgido, ou, se já tiverem surgido, irá manter a existência deles.
Todos os fenômenos condicionados, bem como Nibbana, estão incluidos na
categoria de estados condicionadores. <o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>E como
eles estão relacionados</span></b><span lang=PT-BR style='mso-ansi-language:
PT-BR'>: Isto se refere aos vinte quatro tipos de forças condicionadoras que
operam entre os estados condicionadores e os estados condicionados. Essas
forças condicionadoras são analisadas no Compêndio da Condicionalidade.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O Compêndio
da Condicionalidade está dividido em duas partes:<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> O Método da Origem Dependente<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>O Método das Relações Condicionais<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O Método da
Origem Dependente é caracterizado pela ocorrência de um estado na dependência
de um outro, enquanto que o Método das Relações Condicionais se refere à
eficácia causal específica das condições. <o:p></o:p></span></p>

<p class=Normal><b> <a name=T51></a>O Método da Origem Dependente<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O termo
“origem dependente” é um composto de <i>paticca, </i>na dependência de, e <i>samuppada,
</i>surgimento, origem. Essa expressão em geral se aplica à fórmula dos 12 elos
que com freqüência é encontrada nos suttas. A origem dependente é em essência o
relato da estrutura causal do ciclo de existências demonstrando as condições
que mantêm a roda do nascimento e morte e que faz com que ela gire de uma
existência para outra. Nos Comentários, a origem dependente é definida como o
surgimento de efeitos na dependência de uma conjunção de condições. Isso
significa que não existe uma causa única capaz de produzir um efeito, e nem
somente um efeito surge de uma determinada causa. Ao invés disso, existe sempre
uma coleção de condições dando origem a uma coleção de efeitos. Quando, na
fórmula tradicional é declarado um estado como uma condição para outro, isso é
dito para identificar a condição principal dentre uma coleção de condições e
relacioná-la ao efeito mais importante dentro de uma coleção de efeitos. [<a
href="#N1">1</a>] <o:p></o:p></span></p>

<p class=Normal><b> <a name=R1></a><a name=T52></a>O Método das Relações Condicionais<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Este é o
método desenvolvido no <i>Patthana, </i>O Livro das Relações Condicionais que é
o sétimo e último livro do Abhidhamma Pitaka. Em contraste com o método da
origem dependente, que trata apenas dos estados condicionadores e dos estados
condicionados e a estrutura do seu surgimento, o método do <i>Patthana </i>também
lida com as forças condicionadoras, <i>(paccayasatti). </i>Uma força, <i>(satti),
</i>é aquilo que tem o poder de ocasionar ou realizar um determinado efeito.
Assim como o ardor do chile é inerente ao chile e não pode existir sem ele, do
mesmo modo as forças condicionadoras são inerentes aos estados condicionadores
e não existem sem eles. Todos os estados condicionadores possuem a sua força
particular, e é essa força que permite que eles causem o surgimento dos estados
condicionados. O <i>Patthana </i>traz uma relação de 24 condições com uma
explanação detalhada das várias formas através das quais essas condições
inter-relacionam os fenômenos materiais e mentais enumerados no <i>Dhammasangani,
</i>o primeiro livro do Abhidhamma Pitaka. Para entender de forma apropriada o
ensinamento das relações condicionais contido no Abhidhamma é essencial
entender os três fatores envolvidos em qualquer relação em particular: (1) os
estados condicionadores, <i>(paccayadhamma),</i> fenômenos que funcionam como
condições para outros fenômenos, quer seja produzindo-os, suportando-os ou
mantendo-os; (2) os <i><span style="mso-spacerun: yes"> </span></i>estados
condicionados, <i>(paccayauppannadhamma), </i>pelos estados condicionadores, os
fenômenos que surgem e persistem através da assistência proporcionada pelos
estados condicionadores; (3) a força condicionadora da condição, <i>(paccayasatti),
</i>o modo particular através do qual os estados condicionadores funcionam como
condição para os estados condicionados.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O
significado de <i>Patthana </i>é causa proeminente ou principal. Cada ação tem
uma causa. Existem dois tipos de efeitos: direto e indireto. De acordo com Ledi
Sayadaw, no seu livro ‘<i>Manuals of Buddhism</i>,’ efeito direto quer dizer
efeito primário ou atual; indireto quer dizer efeito conseqüente ou secundário.
Desses dois tipos, apenas o efeito direto é referido como inelutável, pelo fato
dele nunca deixar de surgir quando a sua causa apropriada estiver estabelecida
ou vier à tona. E o efeito indireto deve ser compreendido como falível, já que
ele poderá ou não surgir mesmo que a sua causa esteja plenamente estabelecida.
Portanto, a causa inelutável é assim chamada com referência ao efeito
inelutável. Por conseguinte, apenas a causa inelutável ou principal<span
style="mso-spacerun: yes">  </span>é explicada no <i>Patthana, </i>e daí o seu
nome. Para exemplificar, digamos que a cobiça surja num homem que deseje obter
riquezas. Sob a influência da cobiça ele vai para a floresta onde<span
style="mso-spacerun: yes">  </span>limpa um pedaço de terreno e estabelece um
plantio e se empenha no trabalho. No final das contas, como resultado do seu
trabalho ele obtém muito dinheiro com o qual ele atende as necessidades da sua
família e realiza muitos atos meritórios dos quais ele irá obter recompensas em
existências futuras. Neste exemplo, todos os estados mentais e materiais que
coexistem com a cobiça são chamados de efeitos diretos. Além desses, todas as
conseqüências, resultados e recompensas que serão desfrutados mais tarde são
chamados de efeitos indiretos. Desses dois tipos de efeitos apenas o primeiro é
tratado no <i>Patthana, </i><span style='mso-bidi-font-style:italic'>enquanto
que</span> o segundo é tratado nos discursos do Sutta Pitaka. É necessário
entender que todas as coisas que ocorrem, acontecem, têm lugar ou produzem
mudanças, são apenas os efeitos diretos e indiretos, resultados, conseqüências
ou produtos dessas vinte quatro relações ou causas. <o:p></o:p></span></p>


