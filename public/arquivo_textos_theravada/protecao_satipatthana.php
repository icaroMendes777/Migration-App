<p class=Normal>Certa vez o Buda contou aos bhikkhus a seguinte história (<a
href="../sutta/SNXLVII.19.php">Sedaka Sutta - SN XLVII.19</a>): </p>

<p class=Normal style='margin-left:.75in'> “Bhikkhus, certa vez no passado,
um acrobata, tendo levantado um poste de bambu, se dirigiu ao seu ajudante,
Medakathalika: ‘Venha, estimado Medakathalika, suba nos meus ombros e fique em
pé sobre o poste de bambu.’ Tendo respondido, ‘Sim, mestre,’ o aprendiz
Medakathalika subiu sobre os ombros dele e ficou em pé no poste de bambu. O
acrobata então disse para o aprendiz Medakathalika: ‘Você agora me proteja,
estimado Medakathalika, e eu o protegerei. Assim, guardando um ao outro,
protegendo um ao outro, mostraremos nossa arte, receberemos nossa recompensa e
com segurança desceremos do poste de bambu.’ Quando isso foi dito o aprendiz
Medakathalika respondeu: ‘Esse não é o modo de fazer isso, mestre. Você se
proteje, mestre, e eu me protegerei. Assim, com cada um guardado e protegido,
nós mostraremos a nossa arte, receberemos a nossa recompensa e com segurança desceremos
do poste de bambu.’ <o:p></o:p></p>

<p class=Normal style='margin-left:.75in'> “Esse é o método nesse caso,” o
Abençoado disse. “É exatamente aquilo que o aprendiz Medakathalika disse para o
seu mestre. ‘Eu me protegerei,’ bhikkhus: assim devem ser praticados os
fundamentos da atenção plena. ‘Eu protegerei os outros,’ bhikkhus: assim devem
ser praticados os fundamentos da atenção plena. Protegendo a si mesmo,
bhikkhus, ele protege os outros; protegendo os outros, ele protege a si mesmo.<o:p></o:p></p>

<p class=Normal style='margin-left:.75in'> “E como é, bhikkhus, que
protegendo a si mesmo, ele protege os outros? Através da perseverança,
desenvolvimento e cultivo dos quatro fundamentos da atenção plena. É desse modo
que protegendo a si mesmo, ele protege os outros.<o:p></o:p></p>

<p class=Normal style='margin-left:.75in'> “E como é, bhikkhus, que
protegendo os outros, ele protege a si mesmo? Através da paciência, não fazendo
o mal, através de uma mente com amor bondade e compaixão. É desse modo que
protegendo os outros, ele protege a si mesmo.</p>

<p class=Normal> Este sutta pertence a um grupo considerável de ensinamentos
importantes e eminentemente práticos do Buda, que ainda se encontram escondidos
como se fossem um tesouro enterrado, desconhecidos e não utilizados. Apesar
disso, este texto possui uma importante mensagem para nós, e o fato de ele
estar estampado com o selo real de <i>satipatthana</i> justifica uma atenção
ainda mais especial. </p>

<p class=Normal><b>Indivíduo e Sociedade</b><br>
<p class=Normal>O sutta trata das nossas relações com os demais seres humanos, entre o
indivíduo e a sociedade. Ele resume de forma sucinta a atitude Budista em
relação aos problemas da ética individual e da sociedade, de egoísmo e
altruismo. A sua essência está contida em duas frases concisas: </p>

<p class=Normal style='margin-left:.75in'>&quot;Protegendo a si mesmo, se protege os outros <i>(Attanam
rakkhanto param rakkhati.)</i> </p>

<p class=Normal style='margin-left:.75in'>&quot;Protegendo os outros, se
protege a si mesmo.&quot; <i>(Param rakkhanto attanam rakkhati.)</i> </p>

<p class=Normal>Essas duas frases são complementares e não devem ser tomadas
ou mencionadas em separado. Hoje em dia, quando o serviço social é tão
enfatizado, as pessoas podem ser tentadas a dar sustentação às suas idéias
mencionando apenas a segunda frase. Mas qualquer citação unilateral distorcerá
o ponto de vista do Buda. Deve ser lembrado que na nossa estória o Buda
expressamente aprova as palavras do aprendiz, que primeiro devemos vigiar com
cuidado nossos próprios passos se quisermos proteger os outros do mal. Aquele
que estiver afundado na lama, não será capaz de ajudar os outros a saírem dela.
Nesse sentido, a nossa própria proteção forma a base indispensável para a
proteção e ajuda dada aos outros. Mas a proteção de si mesmo não é uma proteção
egoísta. É auto-controle, é o desenvolvimento ético e espiritual de si mesmo.</p>

<p class=Normal>Existem algumas grandes verdades que são tão abrangentes e profundas
que<span style="mso-spacerun: yes">  </span>parecem ter um alcance que se
expande com o próprio limite do indivíduo de compreedê-las e praticá-las. Tais
verdades se aplicam a vários níveis de compreensão e são válidas em vários
contextos das nossas vidas. Depois de alcançar o primeiro ou segundo nível, a
pessoa se surpreenderá com o fato de que repetidamente novas perspectivas
venham a se abrir à nossa compreensão, iluminadas por essas mesmas verdades.
Isto também se aplica às duas verdades gêmeas do nosso texto, que agora
passaremos a considerar em mais detalhe. </p>

<p class=Normal>&quot;Protegendo a si mesmo, se protege os outros” – a verdade dessa
afirmação tem origem em um nível extremamente simples e prático. O primeiro
nível material dessa verdade é tão evidente que não são necessárias mais do que
algumas palavras a respeito. É óbvio que a proteção da nossa pópria saúde
contribui muito para a proteção da saúde dos outros que compartem o nosso
ambiente, especialmente no que diz respeito a doenças contagiosas. A cautela e
a circunspecção em todas as nossas ações e movimentos irão proteger os outros
dos danos que lhes possam ocorrer pela nossa falta de cuidado e negligência.
Dirigir com cuidado, abstenção de álcool, autocontrole nas situações que possam
conduzir à violência – nessas e em muitas outras maneiras, podemos proteger os
outros ao protegermos a nós mesmos. </p>

<p class=Normal><b>O Nível Ético</b></p>

<p class=Normal>Chegamos agora ao nível ético dessa verdade. A nossa própria proteção moral
irá proteger os outros, os indivíduos e a sociedade, das nossas paixões
desenfreadas e impulsos egoístas. Se permitirmos que as três raízes do mal –
cobiça, raiva e delusão – tomem conta do nosso coração, então os seus rebentos
irão se espalhar em todas as direções como se fossem uma trepadeira, sufocando
tudo aquilo de nobre e saudável que tente crescer à sua volta. Mas se nos
protegermos dessas três raízes, os nossos semelhantes também estarão a salvo.
Eles estarão a salvo da nossa cobiça irresponsável por posses e poder, da nossa
luxúria e sensualidade desenfreadas, da nossa inveja e ciúme; a salvo das
consequências maléficas da nossa raiva e inimizade que podem ser destrutivas ou
até mesmo criminosas; a salvo das nossas explosões de raiva e da atmosfera de
antagonismo e conflito resultante, que pode fazer com que a vida lhes seja
insuportável. </p>

<p class=Normal>Os efeitos maléficos que a nossa cobiça e raiva possuem sobre os outros não
estão limitados às ocasiões em que eles se tornam objetos passivos ou vítimas
da nossa raiva, ou quando as suas posses se tornam o objeto da nossa cobiça.
Ambos a cobiça e a raiva possuem um poder contagioso que multiplica imensamente
os seus efeitos maléficos. Se nós mesmos não pensarmos noutra coisa que não
seja cobiçar e agarrar, adquirir e possuir, tomar e se apegar, então
estimularemos ou reforçaremos esses instintos possessivos nos outros. A nossa
má conduta poderá se tornar o padrão de comportamento para aqueles que nos
cercam – nossos filhos, amigos e colegas. Nossa própria conduta poderá induzir
os outros a que se juntem a nós na satisfação ordinária dos nosssos desejos
predatórios; ou podemos estimular neles sentimentos de ressentimento e
competitividade. Se estivermos plenos de sensualidade, poderemos também
despertar neles o fogo do desejo. A nossa própria raiva poderá provocar neles a
raiva e a vingança. Poderemos também nos aliar com os outros ou instigá-los a
cometer atos ordinários de raiva<span style="mso-spacerun: yes">  </span>e
inimizade. A cobiça e a raiva são de fato iguais a doenças contagiosas. Se nos
protegermos contra essas infecções maléficas, estaremos também, pelo menos até
certo ponto, protegendo os outros. </p>

<p class=Normal><b>Proteção através da Sabedoria</b></p>

<p class=Normal>Quanto à terceira raíz do mal, delusão ou ignorância, sabemos muito bem
quanto dano pode ser causado aos outros através da estupidez, desconsideração,
preconceitos, ilusões e delusões de uma única pessoa . </p>

<p class=Normal>Sem conhecimento e sabedoria, as tentativas de proteger a si mesmo e aos
outros em geral fracassarão. A pessoa verá o perigo apenas quando for muito
tarde, ela não irá se precaver quanto ao futuro; ela não saberá os meios
corretos e efetivos de proteção e ajuda Por isso, a própria proteção através do
conhecimento e sabedoria são da maior importância. Ao obter o verdadeiro
conhecimento e sabedoria, estaremos protegendo os outros das consequências
danosas da nossa própria ignorância, preconceitos, fanatismo infeccioso e
delusões. A história nos mostra que as grandes e destrutivas delusões em massa
foram com frequência acesas por um único indivíduo ou um pequeno grupo de
pessoas. A própria proteção através do conhecimento e sabedoria irá proteger os
outros dos efeitos perniciosos de tais influências. </p>

<p class=Normal>Já indicamos de forma breve como a nossa vida particular pode ter um forte
impacto sobre as vidas dos outros. Se deixarmos sem resolução as fontes reais
ou potenciais de dano social dentro de nós mesmos, nossa atividade social
externa será fútil ou marcadamente incompleta. Portanto, se estivermos
motivados por um espírito de responsabilidade social, não deveríamos nos
esquivar da dura tarefa do nosso próprio desenvolvimento moral e espiritual. A
preocupação com as atividades sociais não deve se converter em uma desculpa ou
escapatória para a tarefa primeira, que é de arrumar a própria casa primeiro. </p>

<p class=Normal><span style="mso-spacerun: yes"> </span>Por outro lado, aquele que com
seriedade se dedica ao seu desenvolvimento moral e espiritual será um força
vigorosa e ativa para o bem no mundo, mesmo que ele não se dedique a alguma
atividade social externa. Só o seu exemplo silencioso irá ajudar e encorajar
muitos, mostrando que os ideais de uma vida abnegada e inofensiva podem na
verdade ser vividos e que não são apenas temas de sermões. </p>

<p class=Normal><b>O Nível Meditativo</b></p>

<p class=Normal>Passamos agora<span style="mso-spacerun: yes">  </span>ao nível mais elevado
de interpretação do nosso texto. Ele está expresso nas seguintes palavras do
sutta: &quot;E como é, bhikkhus, que protegendo a si mesmo, ele protege os
outros? Através da perseverança, desenvolvimento e cultivo dos quatro
fundamentos da atenção plena.&quot;<span style="mso-spacerun: yes">  </span>A
nossa própria proteção moral não terá estabilidade enquanto permanecer como uma
rígida disciplina que se cumpre após um choque entre motivos e contra padrões
conflituosos de pensamento e comportamento. Os desejos apaixonados e as
tendências egoístas podem crescer em intensidade se a pessoa tentar
silenciá-los apenas através da força de vontade. Mesmo havendo êxito temporário
em suprimir impulsos apaixonados ou egoístas, o conflito interno não resolvido
irá impedir o progresso moral e espiritual e deformar o caráter da pessoa. Além
disso, a desarmonia interna provocada pela supressão forçada dos impulsos irá
encontrar uma saída no comportamento externo. Poderá fazer com que o indivíduo
se torne irritadiço, ressentido, dominador e agressivo em relação aos outros.
Portanto, o dano pode vir para si mesmo bem como para os outros devido a um
método equivocado de proteção de si mesmo. Somente quando a própria proteção
moral tenha se tornado uma ação <i>espontânea; </i>quando ela surja tão
naturalmente como o fechamento protetor da pálpebra contra a poeira – somente
então a nossa estatura moral irá proporcionar proteção e segurança reais para
nós e para os outros. Essa conduta virtuosa natural não nos é dada como um
presente dos céus. Ela tem que ser conquistada através da repetida prática e
cultivo. Portanto o nosso sutta diz que é através da prática repetida que a
própria proteção se torna suficientemente forte para proteger os outros
também.<span style="mso-spacerun: yes">  </span></p>

<p class=Normal>Mas se essa prática repetida ocorrer apenas nos níveis prático, emocional e
intelectual, as suas raízes não serão suficientemente firmes e profundas. Essa
prática repetida também deve ser extendida ao nível do cultivo da meditação.
Com a meditação, os motivos práticos, emocionais e intelectuais da nossa
própria proteção moral e espiritual se tornarão nossa propriedade pessoal que
não poderá ser perdida com facilidade. Portanto, neste caso, o nosso sutta está
se referindo a <i>bhavana</i>, o desenvolvimento meditativo em seu sentido mais
amplo. Essa é a forma mais elevada de proteção que o nosso mundo pode oferecer.
Aquele que desenvolveu a sua mente com a meditação vive em paz consigo mesmo e
com o mundo. Nele, nenhum mal ou violência terá origem. A paz e a pureza que
ele irradia terá um poder inspirador, elator e será uma benção para o mundo.
Ele será um fator positivo na sociedade, mesmo se viver em isolamento e em
silêncio. Quando a compreensão e o reconhecimento do valor social de uma vida
dedicada à meditação cessarem em uma nação, esse será um dos primeiros sintomas
da deterioração espiritual. </p>

<p class=Normal><b>Proteção dos Outros</b></p>

<p class=Normal>Agora deveremos considerar a segunda parte da declaração do Buda, um
complemento necessário à primeira: &quot;E como é, bhikkhus, que protegendo os
outros, ele protege a si mesmo? Através da paciência, não fazendo o mal,
através de uma mente com amor bondade e compaixão <i>(khantiya avihimsaya
mettataya anuddayataya).&quot;</i> </p>

<p class=Normal>Aquele cujo relacionamento com os seus semelhantes for governado por esses
princípios estará melhor protegido do que<span style="mso-spacerun: yes"> 
</span>com a força física ou com armas poderosas. Aquele que é paciente e tolerante
evitará conflitos e disputas e fará amigos daqueles para com os quais tenha
demonstrado uma compreensão paciente. Aquele que não apela para a força ou
coersão raramente se tornará, sob circunstâncias normais, um objeto de
violência já que ele não provoca a violência nos outros. E se ele for
confrontado com a violência, fará com que ela termine logo pois não irá perpetuar
a hostilidade através da vingança. Aquele que possui amor e compaixão por todos
os seres, livre de inimizade, superará a má vontade dos outros e desarmará os
brutos e violentos. Um coração compassivo é o refúgio de todo o mundo. </p>

<p class=Normal>Podemos agora compreender como essas duas sentenças complementares do nosso
texto se harmonizam. A proteção de si mesmo é a base indispensável. Mas a
verdadeira proteção de si mesmo só é possível se não conflitar com a proteção
dos outros; pois aquele que busca a sua própria proteção às expensas dos outros
se tornará impuro e se colocará em perigo. Por outro lado, a proteção dos
outros não deve conflitar com os quatro princípios da paciência, não fazer o mal,
amor bondade e compaixão; e também não deve interferir com o livre
desenvolvimento espiritual como é o caso de várias doutrinas totalitárias.
Portanto, o conceito Budista de proteção de si mesmo exclui todo o egoísmo, e
na proteção dos outros a violência e a interferência não têm lugar. </p>

<p class=Normal>A proteção de si mesmo e a proteção dos outros correspondem às duas grandes
virtudes gêmeas do Budismo, sabedoria e compaixão. A proteção correta de si
mesmo é a expressão da sabedoria, a proteção correta dos outros<span
style="mso-spacerun: yes">  </span>é a expressão da compaixão. Sabedoria e
compaixão, sendo os elementos primários da Iluminação ou Bodhi, encontraram a
perfeição mais elevada no Perfeitamente Iluminado, o Buda. A insistência
no<span style="mso-spacerun: yes">  </span>desenvolvimento harmonioso de ambas
é uma aspecto característico de todo o Dhamma. Nós as encontramos nos quatro
estados sublimes <i>(brahmavihara),</i> em que a equanimidade corresponde à
sabedoria e à auto proteção, enquanto que o amor bondade, a compaixão e a
alegria altruísta correspondem à compaixão e à proteção dos outros. </p>

<p class=Normal>Esses dois grandes princípios, da proteção de si mesmo e proteção dos
outros, possuem a mesma importância para ambas, a ética do indivíduo e da
sociedade, e harmonizam os objetivos de ambas. O seu impacto benéfico, no
entanto, não se limita ao nível ético, mas conduz o indivíduo para cima, para
as superiores realizações do Dhamma, enquanto que ao mesmo tempo provê um
sólido fundamento para o bem estar da sociedade. </p>

<p class=Normal>O autor crê que a compreensão desses dois grandes princípios, da proteção de
si mesmo e<span style="mso-spacerun: yes">  </span>proteção dos outros como
manifestação das virtudes gêmeas de sabedoria e compaixão, é de vital
importância para a educação Budista, tanto para os jovens como para os idosos.
Elas são as pedras fundamentais para a construção do caráter e merecem um papel
central no esforço global para o renascimento do Budismo. </p>

<p class=Normal>“Eu protegerei os outros” – assim deveríamos estabelecer a nossa atenção
plena e guiados por ela, dedicarmo-nos à prática de meditação em benefício da
nossa própria libertação. </p>

<p class=Normal>“Eu protegerei os outros” – assim deveríamos estabelecer a nossa atenção
plena e guiados por ela, controlar a nossa conduta através da paciência, boas ações,
amor bondade e compaixão, pelo benefício e felicidade de muitos. </p>


