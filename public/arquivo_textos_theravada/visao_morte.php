<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><B>MB:</B> Qual é
a visão geral Budista sobre a morte?<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>Bhante
Gunaratana:</b> Primeiro, a definição. Quando a força vital – calor e consciência –
cessam de existir, a isso chamamos de morte. A morte pode ocorrer: (1) quando o
kamma da pessoa estiver esgotado; (2) quando o tempo de vida da pessoa estiver
esgotado, isto é, a duração daquela vida em particular ( a pessoa só pode viver
aquele tanto e depois disso tem que morrer); (3) Quando ambos o kamma e o tempo
de vida se esgotam juntos; (4) quando a vida termina devido a acidentes, causas
não naturais. Essas são as maneiras como a morte pode ocorrer.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Mas no
Budismo a morte não é o fim completo da existência. A morte é apenas o
encerramento de um capítulo e o capítulo seguinte é aberto imediatamente em
seguida. Esses dois sempre ocorrem imediatamente em seguida – morte e
renascimento.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>Sudhamma:</b>
As pessoas estão sempre<span style="mso-spacerun: yes">  </span>preocupadas com
como se preparar para a morte. Talvez existam dois níveis que devam ser tomados
em conta. Sendo que, um é sobre como preparar a mente comum para esse evento,
ou, o que é adequado fazer no momento da morte. E o outro é como superar o
ciclo de nascimento e morte. Em outras palavras, como se preparar para a morte,
não estando mais sujeito à morte .<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>Bhante
Gunaratana:</b> Entendo. Queremos conversar sobre dois tipos de morte. Uma é a
morte convencional e a outra é a morte final. A morte convencional também tem
dois lados, uma é a morte momento a momento, a outra é a morte “real” da qual
não é possível reviver. Na morte momento a momento, parece que você sobreviveu.
Você ainda existe. Mas na verdade, tudo no corpo e na mente está morrendo a
cada instante. E está sendo renovado – está renascendo. A repetição de morte e
renascimento não parece estar ocorrendo. Parece que estamos vivos. Mas você tem
que compreender que a morte ocorre a cada instante. Compreender essa verdade é
o passo mais importante na preparação para a morte. Isso é compreender o
significado da morte. Se compreendermos isso, compreenderemos que a morte
“real” é apenas mais um momento. E até aquele momento eu já terei morrido
trilhões de vezes. Cada um desses momentos foi uma morte momentânea.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><B>MB:</B> E isso,
creio, é o que compreendemos através da meditação?<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>Bhante Gunaratana:</b>
Sim, isso é o que compreendemos através da meditação. Na verdade, se você
pratica meditação de insight e<span style="mso-spacerun: yes"> 
</span>meditação de concentração, uma ajuda a entender a outra. Assim, se você
tiver um insight irá compreender o que é concentração. Se você estiver
concentrado irá compreender o que é<span style="mso-spacerun: yes"> 
</span>insight. Com a mente concentrada e com<span style="mso-spacerun: yes"> 
</span>atenção plena, você realmente sente, experimenta e compreende essa morte
momentânea. Você sente as palpitações, o batimento cardíaco, as vibrações
nervosas, as sensações mudando, as percepções mudando e cada pensamento
mudando.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Mudança,
significa unicamente morte momentânea. Quando as coisas mudam, elas nunca podem
ser revividas. Quando um pensamento momentâneo está morto, aquele pensamento
momentâneo nunca mais irá reaparecer. A morte de uma célula quer dizer que
quando ela morre, está terminada. Aquela célula nunca tornará a reviver. Ela
não deve permanecer no corpo. Tem que ser descartada, expelida do corpo, para
que outras células possam crescer e se desenvolver. Se elas não forem
descartadas pelo corpo, elas crescerão em número e poderemos desenvolver
câncer.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Uma vez
morta, a célula tem de ser descartada, exatamente como quando o corpo humano
morre, ele tem que ser descartado. Tem que ser tirado da casa. Tem que ser
levado para algum lugar para ser enterrado ou queimado para que os demais
possam viver uma vida saudável, higiênica.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Esse tipo
de coisa está ocorrendo o tempo todo. Através da meditação Vipassana vemos a
morte momento a momento, nós a experimentamos, nós a conhecemos,
tornamo-nos<span style="mso-spacerun: yes">  </span>plenamente conscientes
dela, e essa é a maneira de se preparar para a morte. Assim nos preparamos para
a morte “real” no nível experiencial.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Além disso,
nos preparamos para a morte “real” encarando-a de forma lógica. Você apenas precisa
abrir os olhos e olhar à sua volta. Tudo está morrendo o tempo todo. Você pode
notar que as árvores, plantas e insetos morrem o tempo todo. Ao ter vivido
quarenta anos, por exemplo, se você contar o número de amigos, parentes e
conhecidos que já morreram, você deveria um dia sentar e pensar, “Desta maneira
eu devo entender que com a quantidade de amigos, parentes e conhecidos – com
todas essas pessoas que já morreram, em algum momento será a minha vez.
Portanto, eu sei que irei morrer, meus amigos, colegas de escola, um a um, um a
um, morreram. A minha vez deve estar chegando.” Essa é uma outra forma de
encarar a morte.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Uma outra
maneira lógica de encarar a morte é pensar que todos nós somos feitos de
elementos impermanentes. Por exemplo, nós somos constituídos dos elementos:
terra, água, fogo e ar. No entanto, amanhã eles não estarão mais presentes.
Conseqüentemente, esses elementos não serão nunca permanentes. Os elementos que
constituem o corpo estão sujeitos à morte, à impermanência. Por isso o seu
produto também será impermanente. Não há como parar isso. Então, examinar os
elementos que constituem o corpo é uma outra maneira lógica de encarar a morte.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Uma vez que
tenhamos compreendido a verdade da morte, deveríamos pensar, “Agora que irei
morrer, porque deveria ter orgulho de alguma coisa? Estou intimidado pelo
pensamento da morte, não tenho nenhum motivo para ter orgulho de nada. Não
tenho motivo para estar ressentido com ninguém. Cedo ou tarde irei morrer e não
tenho motivo para tentar apegar-me a qualquer coisa. Não importa a firmeza com
a qual eu me apegue ao que quer que seja, isso irá escapar da minha mão no
momento da separação. Portanto, também não preciso da cobiça. Penso que, se não
me apegar à cobiça, minha morte será bem pacífica.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A próxima
coisa a pensar é, “Eu sei que irei morrer, penso que é bom morrer de forma
pacífica, portanto devo me preparar para isso. Que eu esteja em paz o tempo
todo.” Isso não quer dizer que você irá deitar no meio da rua para ser
atropelado ou que você deve tomar veneno, ou se suicidar. Essa não é a forma de
obter paz. Temos que viver esta vida enquanto ela durar. Temos que continuar
com o que estamos fazendo. Portanto, precisamos pensar, “Como irei morrer de
qualquer jeito, devo morrer em paz.” Para morrer em paz precisamos preparar as
nossas mentes para estarem em paz. Uma morte pacífica é uma morte sem dor.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><B>MB:</B> Você
poderia nos falar um pouco sobre a dor física devido à enfermidade?<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>Bhante G:</b>
Sim, quando meditamos sentimos dor física, por exemplo, dores nos joelhos.
Podemos usar essa dor para nos prepararmos para a dor de uma enfermidade
terminal. A dor no joelho é igual à dor de um câncer. Penso que se um câncer
afetar o nosso sistema nervoso então sentiremos dor o tempo todo. O que quer
que façamos, os nervos estando expostos, sentiremos dor. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Por isso
precisamos preparar as nossas mentes aprendendo a praticar a meditação das
sensações. Determinamo-nos a observar sempre as nossas sensações, seja ela qual
for, mesmo uma pequena dor de dente, dor no pescoço, qualquer dor por menor que
seja. Se estamos experimentando dor, meditamos sobre ela. Quando a dor surge,
nós focamos a atenção nela. Vemos como ela surge, quanto tempo permanece, e
depois nós a vemos desaparecer. Por menor que seja a dor, ela possui sempre
esses três estágios, o seu momento de surgimento, o seu momento de pico e o seu
momento de desaparecimento.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Se
condicionarmos a nossa mente a permanecer com a dor, ou com aquela sensação,
então a nossa mente poderá ser absorvida por aquela sensação e tornar-se uma só
com<span style="mso-spacerun: yes">  </span>ela. Se nos voltarmos contra a dor,
estaremos nos dividindo. Mas se tentarmos aceitar tudo que surgir, iremos nos
absorver naquilo. Mesmo quando sentimos uma dor intensa, chegaremos a um ponto
em que a mente não é mais capaz de tolerar a dor e então a mente se funde com a
dor. Depois disso, o que acontece já não tem importância.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim,
antes que a morte ocorra, aprendemos a aceitar e a permanecer de bom grado com
a dor, aprendemos a observá-la e a não ficar preocupado com ela. Quanto mais
nos preocuparmos com a dor, mais doloroso será. Quanto mais relaxarmos com a
sensação, menos doloroso será. Tive alguns amigos que morreram com dores
físicas terríveis. Eles se recusavam a tomar medicamentos. Apesar disso, eles
até explicavam para as visitas onde o câncer estava, como havia se desenvolvido
e em que estágio se encontrava. Ao invés das visitas consolarem o paciente, era
o paciente quem consolava as visitas.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O paciente
pensava que as visitas vinham por compaixão, simpatia, para encorajá-lo, mas ao
relaxar com a dor, era ele quem dava compaixão, simpatia e encorajamento para
as visitas. Deste modo, a dor física numa enfermidade terminal não é
necessariamente um obstáculo para uma morte pacífica.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><B>MB:</B> Mas e
quando a pessoa ultrapassou o limite tolerável da dor, quando se torna
impossível relaxar e absorver-se na dor?<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>Bhante G:</b>
Você sabe, existem dores que uma pessoa é incapaz de tolerar e nesse caso a
medicação é necessária. Mas nós podemos primeiro tentar aumentar a tolerância à
dor condicionando a mente e preparando-a para aceitar a dor física. Podemos
condicionar a mente aconselhando de forma gentil e suave que a pessoa medite.
Podemos entoar alguns cânticos que acalmam e tranqüilizam, tocar música que
acalme e tranqüilize para preparar a mente, para ajudar a mente a ficar em paz.
Dar-lhe instruções sobre como meditar.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Não se
esqueça que quando as mulheres sentem as dores do parto são orientadas para
concentrar a atenção na respiração. Elas mantêm a respiração dentro de um certo
ritmo. Ao empurrar o bebê para fora elas estão focando na respiração, no corpo
e no ato de empurrar.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Essa é uma
coisa muito bela para ser lembrada. Podemos usar essa informação para ensinar
as pessoas. E essas mulheres dão à luz aos seus bebês com menos dor porque elas
foram treinadas nesse procedimento. Assim treinamos a mente a aceitar a dor.
Podemos usar essa informação das mulheres dando à luz para ensinar as pessoas
que estão morrendo como lidar com a dor.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim, temos
que treinar a mente. Ao invés de primeiro lidar com a dor física, aprendemos a
lidar primeiro com a mente. Como esses dois estão sempre cooperando um com o
outro, quando o corpo se acalma, a mente se acalma. Eles estão sempre se
complementando.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><B>MB:</B> Qual a
sua opinião quanto às drogas usadas para eliminar a dor?<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>Bhante G:</b>
Eu penso que elas possuem efeitos colaterais. Elas podem reduzir a sua pressão
arterial. Algumas pessoas não podem tomá-las, e elas podem ser muito perigosas.
Há um limite para a dosagem que pode ser mantida na corrente sanguínea da
pessoa.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Mas a
meditação foi inventada muito antes da descoberta dos medicamentos. Nos dias de
hoje as pessoas não dão muita atenção ao treinamento espiritual e vão direto
para os narcóticos ou drogas para eliminar a dor. Agora queremos inverter a
ordem porque o tratamento espiritual é mais saudável que o tratamento químico.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>As pessoas
tomam todo tipo de pílulas, você sabe, e<span style="mso-spacerun: yes"> 
</span>todas elas têm efeitos colaterais, particularmente depois de usadas
durante muito tempo. Mas o treinamento espiritual nunca terá efeitos
colaterais. Ele é sempre positivo. Sempre prolonga a sua vida. Mas se ele tiver
qualquer efeito colateral, será para melhorar a sua vida, dando um novo sabor a
ela. Se você realizar essas práticas enquanto enfermo, ao se recuperar, elas
produzirão um efeito pacífico que se prolongará para além da recuperação
daquele estado doloroso.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Agora, para
retornar ao tema da morte. Na verdade nos desviamos um pouco, porque a
enfermidade pode ser a causa da morte e a enfermidade provoca dor. Morrer de
forma rápida, sem dor, não é nenhum problema. Por esta razão, creio que falar
sobre a dor é essencial em qualquer discussão sobre a morte.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>Sudhamma:</b>
Falamos sobre dois tipos de morte: momento a momento e a “real.” Em certo ponto
você falou sobre abandonar os ressentimentos e a cobiça e ter uma mente
pacífica. O que você queria dizer com isso? <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>Bhante G:</b>
Gostaria de falar sobre a morte permanente, isto é, morrer para nunca mais
renascer. Vocês estão cansados desses nascimentos e mortes, momento a momento,
vida após vida. Assim, um momento inicia e termina e um outro inicia e termina.
Uma vida inicia e termina e outra inicia e termina. Ficamos cansados disso
tudo. Então queremos a morte e não renascer nunca mais.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A morte
causa o nascimento porque existe o desejo de renascer. Enquanto existir esse
desejo, haverá o renascimento. Quando o desejo de renascer estiver extinto,
então não haverá mais renascimento.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>E isso nos
leva ao último estágio da iluminação.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>Sudhamma:</b>
Quando você estava falando sobre a morte, você falou sobre “fazer com que a
mente fique em paz” e disse algo sobre “Que eu tenha uma mente pacífica. Que eu
tenha uma morte pacífica.” Esse tema foi concluído?<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>Bhante G:</b>
Sim, mas eu gostaria de mencionar uma outra coisa. Quando a morte está se
aproximando, com freqüência a pessoa sente remorso, pesar e culpa. Essa é uma
outra razão para sentir medo. Como ela sabe que irá renascer e que fez muitas
coisas erradas, no momento da morte ela se lembra disso. A isto se denomina “o
pensamento mais próximo da morte”. Nesse pensamento, naquele instante que
antecede a morte, ela se lembra de forma clara, como um relâmpago, de certas
coisas que aconteceram durante a sua vida. A morte também é dolorosa sob o
ponto de vista psicológico.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então,
quando uma pessoa está morrendo, pessoas cheias de compaixão ajudam-na a ter
uma morte pacífica contando-lhe as boas coisas que ela fez. Por exemplo, se ela
criou filhos, elas poderão falar- lhe sobre o quanto ela fez pelas crianças e
sobre todas as outras coisas boas que ela tenha feito para os outros. Se ela
tiver irmãos e irmãs, elas poderão relembrar as boa ações que ela tenha
realizado. Qualquer coisa positiva que ela tenha feito, tal como, plantar
árvores, limpar a rua, podem ser lembradas, com convicção.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Além disso,
elas podem sugerir que ela pense em um objeto que traga paz, como o Buda, uma
luz celestial, a tranqüilidade e a paz, a alegria que teve na vida, para tentar
bloquear os pensamentos negativos. Da mesma forma, se você conhecer uma pessoa
religiosa, um monge ou sacerdote ou alguém semelhante, chame-o e peça que ele
faça um sermão. Embora a pessoa que esteja morrendo possa no passado ter odiado
sermões, agora ela irá ouvir os sermões com vontade, pois não existe nada mais
a ser feito.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style="mso-spacerun: yes"> </span>Essas são, portanto, as coisas que uma pessoa
que esteja morrendo pode fazer e que outras pessoas podem fazer para ajudá-la a
morrer em paz.<o:p></o:p></span></p>

<p class=Normal><B>MB:</B> Eu estive lendo um livro de Philip Kapleau (<i>The Wheel
of Birth and Death</i>). <span lang=PT-BR style='mso-ansi-language:PT-BR'>Ele
fala de um modo que me deixou com dúvidas sobre o eu, o não-eu e a morte.
Depois de ler o livro eu pensei, “Como pode existir a morte, se não existe nada
para morrer?”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>Bhante G:</b>
Isso soa bastante abstrato – dizer que não existe nada para morrer. Essa é a
base filosófica do que estamos fazendo. Em última análise, nada existe. E
quando nada existe, não existe nada para morrer.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Mas você
tem que ter um estado mental muito poderoso para ter esse tipo de pensamento no
momento da morte. Muito antes da morte, enquanto ainda estiver saudável, você
poderá ter esse tipo de pensamento, mas no momento da morte os seus sentidos
estarão enfraquecidos. A sua capacidade para pensar estará enfraquecida. Quando
você está à beira da morte, tudo fica mais fraco.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>De acordo
com o Abhidhamma, existe neste momento um fluxo fraco de consciência. O processo
cognitivo é mais curto. Em outras circunstâncias, o processo cognitivo possui
dezessete momentos. Quando vem a morte, o processo cognitivo tem quinze ou
treze momentos. Como tudo está muito fraco, quando você está quase morto, o que
resta é apenas um pouco de consciência. Você não está interessado em nada nessa
situação. Eu não creio que a filosofia poderia ajudar muito.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><B>MB:</B> Eu
também estive lendo um livro do Stephen Levine (<i>Who dies? An investigation
of conscious living and conscious dying</i>) e ele diz que se você conseguir
compreender que de todos os modos não existe nada a que se apegar, você não
terá tantos problemas para se soltar das coisas no fim. Nesse livro ele
apresenta muitas meditações sobre como se soltar das coisas.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>Bhante G:</b>
Essa é uma boa idéia. A pessoa precisa ser lembrada do fato de que ela comeu
tantas vezes ao longo da sua vida e que todas aquelas refeições se foram. O que
restou delas agora? Todas as atividades, todos os pensamentos, todas as posses
materiais se foram.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Portanto,
não importa quanto tentemos nos agarrar a algo, isso irá escapar das nossas
mãos. E é uma boa idéia fazer com que as pessoas tenham consciência disso.
Quanto mais tempo você se apegar mentalmente, mais doloroso será. É como
apertar o punho. Quanto mais você aperta o punho mais doloroso é. Ao abrir o
punho e relaxá-lo você sentirá alívio e conforto.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Do mesmo
modo, neste momento, se você liberasse a sua ansiedade, tensão, aperto,
sentiria o alívio da dor, relaxamento. Esse é um bom pensamento – soltar-se das
coisas. E essa é outra coisa que fazemos na meditação Vipassana. Soltamo-nos
das coisas. Desfrutamos delas enquanto nos dão prazer, mas não nos agarramos a
elas. Coma, se for saboroso e desfrute o sabor! Mas se não for saboroso, se for
desagradável, não se agarre nisso!<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>Sudhamma:</b>
Quando você começou a falar sobre a morte disse que havia a morte convencional
e a morte final. Fale sobre a morte final.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>Bhante G:</b> A
morte final é a morte da pessoa iluminada. Uma pessoa iluminada tem o seguinte
tipo de pensamentos. Primeiro ela pensa, “Bem, fiz o que devia ser feito. Não
há mais nada por fazer.” Esse é um dos mais perfeitos e maravilhosos
pensamentos para se ter na mente. Podemos morrer a qualquer momento. Não
devemos esperar para ter esse pensamento. A qualquer momento podemos pensar,
“Fiz tantas coisas na minha vida. Essas são as coisas que eu deveria ter feito
e eu as fiz. O que estou fazendo agora são coisas adicionais, tarefas
adicionais em relação às minhas tarefas originais. Posso muito bem ficar sem
essas tarefas adicionais.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>As tarefas
adicionais são os serviços prestados ao mundo por uma pessoa iluminada. Ela não
precisa deles, mas o corpo e a mente ali estão, e existem seres que sofrem,
então porque não viver de forma útil e com atenção plena para o benefício
deles. Assim ele diz, “Para mim não há nada mais a ser feito. Eu fiz tudo.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Segundo, e
esse é o pensamento racional da pessoa iluminada, ele está baseado na
libertação da mente da pessoa iluminada. Ela pensa, “Estou liberada.” Esse tipo
de pensamento aparece na sua mente sem qualquer esforço, naturalmente. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Por outro
lado, sempre que ela pensa no seu corpo, ela compreende a sua natureza e isso
não gera apego. Ela se encontra num estado de soltar-se de todas as coisas.
Deste modo, ela não tem nada pessoal a que se apegar – nenhum ser ou
pensamento, ou coisa. Isso também ocorre de forma natural.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Mas em
outros casos, apesar de ter se soltado de tudo, a pessoa pode ainda possuir o
desejo de renascer. Talvez ela queira renascer num lugar melhor ou se viveu
esta vida com serenidade, teve uma vida ideal com uma esposa ideal, poderá
dizer, “Gostaria de ter essa esposa na minha próxima vida. Gostaria de renascer
e ter o mesmo tipo de vida que tive, o mesmo conforto, a mesma satisfação
emocional, espiritual que fez esta vida tão pacífica. Portanto, que eu tenha
esta vida novamente.” Então não importa quão nobre a pessoa seja, ela terá a
mesma vida novamente. Ela irá renascer porque, a despeito da sua nobreza, ainda
possui desejo.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Mas uma
pessoa iluminada é mais nobre. Quem está liberado não possui sequer esse
desejo. Ele sabe que até mesmo esse desejo é criado pela mente. Que isso é <a
name=PVW><i>sankhara</i></a> (formação mental condicionada e impermanente).<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Qualquer <i>sankhara</i>,
não importa quão completo e benéfico aparente ser, é impermanente, Além disso,
uma pessoa iluminada sabe que a sua morte está extinta, isto é, que ela nunca
irá morrer outra vez. Para morrer outra vez é necessário nascer outra vez.
“Portanto esta é a minha última morte. Este é o meu último nascimento. Para mim
não há mais nascimento, não há mais morte. Não há nada além disso.” Ele alcança
a sua realização.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Por
conseguinte, isso é o que chamamos de morte final.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando uma
pessoa iluminada se aproxima da morte final, ela não necessita de nenhum meio
de consolo à sua volta para ajudá-la, isto é, mestres e outros para consolar o
seu corpo e a sua mente.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Essa pessoa
não terá nenhuma recordação daquelas que surgem no momento próximo da morte.
Uma pessoa comum se lembra das coisas que fez, seu kamma; e teme o lugar onde
irá renascer. A isto se denomina o “sinal.” Ela terá o sinal do lugar em que
irá renascer. Isso significa que no momento da morte, se você for renascer,
pode ser que veja a sua mãe humana. Se for renascer como animal, verá o animal.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>Sudhamma:</b>
Como uma face?<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>Bhante G:</b>
Como uma visão.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>Sudhamma:</b>
Uma pessoa? Um útero?<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>Bhante G:</b>
Se for um renascimento humano, talvez umidade, como num útero. Pode ser que
você veja um útero e sinta como é estar num útero. Se você for renascer como
humano poderá ver isso. Se for renascer como um ser divino, verá um lugar cheio
de paz.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Mas, quando
a morte final se aproxima, você não vê nenhum desses sinais. Por isso é chamada
“sem sinais.” Não existe um sinal naquele momento.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>E essa é a
visão Budista do nascimento, morte e libertação.<o:p></o:p></span></p>


