<p class=Normal>   O núcleo da disciplina monástica é uma lista de regras chamada <i>Patimokkha</i>. No <i>Patimokkha</i> dos bhikkhus (para os monges) há 227 regras, enquanto no <i>Patimokkha</i> das bhikkhunis (para as monjas) há 311 regras. As quatro primeiras regras no <i>Patimokkha</i>, para ambos monges e monjas, são os quatro <i>Parajika</i>. A palavra <i>Parajika</i> (no antigo idioma indiano chamado Pali) é geralmente traduzida como "fazer com que o agente seja derrotado". Em termos práticos significa que o infrator tem que deixar de vestir, ou perder, o manto. Não é necessária nenhuma cerimônia ou julgamento. A partir do momento em que a transgressão é praticada, o transgressor perde automaticamente o seu status como monge ou monja Budista. Obviamente, estas quatro regras foram consideradas pelo Buda como violações graves da ética espiritual e um obstáculo significativo no caminho para a iluminação. Esse foi considerado um comportamento tão grave por parte de um monge ou monja que a pena de perda do manto se aplica para toda a vida! Tal pessoa simplesmente não pode ser re-ordenada após um período de punição.</p>

<p class=Normal>    As quatro transgressões que implicam <i>Parajika</i>, a pena de perda automática do manto são:</p>

<p class=Normal style='margin-left:1.0in'>        1 . Tomar parte em relações sexuais com outro ser de qualquer sexo.</p>

<p class=Normal style='margin-left:1.0in'>        2 . Roubar algo de valor (incluindo o contrabando, fraude ou deliberadamente evitar o pagamento de algum imposto).</p>

<p class=Normal style='margin-left:1.0in'>        3. Propositadamente matar um ser humano ou encorajar alguém a cometer o suicídio (isto inclui incitar alguém a assassinar outra pessoa e também inclui convencer uma mulher a fazer um aborto).</p>

<p class=Normal style='margin-left:1.0in'>        4 . Gabar-se de ter alcançado uma grande realização espiritual, sabendo que isso não é verdade. Por exemplo, alegar ter realizado a iluminação, de ser o Buda Maitreya, ter alcançado os Jhanas, ou a capacidade de ler a mente, quando a pessoa sabe que não realizou nenhum desses estados.</p>

<p class=Normal>    Se alguém souber que um monge ou monja praticou algum desses atos, então já poderá saber que ele ou ela não mais possui o status de um monge ou monja Budista. Ele ou ela deve deixar os mantos de lado. Se tentar esconder essa transgressão e não abandonar os mantos, é dito que o karma ruim produzido é de fato extremo!</p>

 <p class=Normal>   No caso dessas quatro transgressões o desconhecimento não é desculpa. Nas escrituras Budistas há uma história que exemplifica isso: [<a href="#N1">1</a>] um monge recém-ordenado que não tinha ainda sido instruído no Vinaya foi seduzido por sua ex-mulher a ter relações sexuais. Quando ele disse aos outros monges sobre o ocorrido, os monges foram até o Buda e perguntaram o que deveria ser feito. O Buda decretou que o monge infrator tinha que deixar os mantos e que no futuro, os quatro <i>Parajika</i> deveriam ser explicados a todos os monges imediatamente depois da ordenação. De fato, a orientação para um monge noviço sobre essas quatro regras tornou-se parte da própria cerimônia de ordenação. Portanto, não há desculpa!</p>

