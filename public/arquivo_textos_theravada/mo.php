<p class=Normal>Mo era um detento em uma prisão de segurança máxima, conhecida como Prisão Samsara. A prisão era cercada por cinco muros altos, cada um com a sua própria câmara focada em Mo, e impiedosamente comandada por um sádico guarda presidiário que todos chamavam de Voli. O guarda tirânico fazia com que Mo fosse incessantemente de um lado para o outro, sem nunca lhe dar nenhuma paz, mesmo durante o sono. Mo havia nascido naquela prisão, vivido ali toda a sua vida, e por isso ele não conhecia nenhum outro lugar. Ele até mesmo pensava que Voli, o guarda mandão, era seu amigo e que de vez em quando ele era capaz de influenciá-lo.</p>

<p class=Normal>Certo dia Mo descobriu um túnel na sua cela, exatamente onde ele com freqüência sentava sonhando acordado. O túnel parecia ser muito antigo, porque tinha uma placa em uma língua desconhecida que dizia ARIYA ATTHANGIKA MAGGA. O ex-presidiário que havia construído o túnel até tinha rabiscado o seu nome na pedra "cavado por Zak Yamuni".  "Boa, Zak", pensou Mo.</p>

<p class=Normal>Inicialmente Mo não ficou interessado no túnel. Porque sair pelo túnel quando ele estava tão acostumado à vida na prisão? A Prisão Samsara tinha um pátio enorme para exercícios conhecido como  "Mundo", que ela ainda nem tinha explorado totalmente. Ele tinha um bom emprego na gestão de um negócio na prisão, e até mesmo tinha se casado com uma bela prisioneira chamada Carma Raga que lhe dera três pequenos delinqüentes. Ele amava Carma, muito embora tivessem discussões todos os dias.</p>

<p class=Normal>Certo fim de semana, por curiosidade, Mo explorou os primeiros metros do túnel e descobriu que o túnel era muito mais legal e tranqüilo do que qualquer coisa que ele já tivesse visto antes. Assim ele foi mais fundo e encontrou o livro guia do túnel chamado Sutta Pitaka. </p>

<p class=Normal>Primeiro, dizia o guia do túnel, é necessário ter o entendimento correto, o que significa aceitar, mesmo que com base na fé, que a Prisão Samsara é sofrimento. Além disso, qualquer pessoa pode rastejar pelo túnel com base no seu próprio esforço hábil (chamado de karma no guia) e chegar a um lugar legal chamado Nirvana que está  além dos muros da prisão Samsara.</p>

<p class=Normal>Em segundo lugar, é necessário ter o pensamento correto, ou seja, ele teria que pensar em abrir mão, deixar tudo para trás, porque o túnel é muito estreito para possibilitar levar qualquer tipo de posse. Mo ficou muito perturbado pois o guia dizia especificamente que ele não poderia levar a sua esposa Carma Raga, porque o túnel era apenas grande o suficiente para que ele entrasse sozinho. Em seguida, ele teria que ser gentil, porque alguém que estivesse dentro do túnel enraivecido, bateria a cabeça no teto baixo e ficaria desorientado. Por último, ele não deveria se mover de modo brusco, mas com muita sutileza, para que o guarda da prisão, Voli, não interrompesse a sua fuga.</p>

<p class=Normal>Em terceiro lugar é necessário usar a linguagem correta. O guia do túnel mencionava que mentirosos, maliciosos, grosseiros e até mesmo fofoqueiros, incomodariam os ratos do túnel que iriam persegui-lo de volta para a sua cela.</p>

<p class=Normal>Em quarto lugar é necessário agir de modo  correto. Se ele matasse qualquer tipo de ser, como aqueles ratos, roubasse ou cometesse adultério, no túnel há cobras que iriam ouvir aquilo e não permitiriam que ele passasse. Mesmo beber álcool ou tomar drogas é proibido no túnel, já que ele ficaria desorientado e incapaz de encontrar a saída. </p>

<p class=Normal>Quinto, o modo de vida correto seria essencial. As pessoas que fazem negócios ilícitos acabam pegando o caminho errado no túnel.</p>

<p class=Normal>Em sexto lugar, dizia o guia do túnel, seria necessário o esforço correto. Às vezes é necessário trabalhar duro, às vezes apenas relaxar. Essa foi a instrução mais difícil para Mo compreender. Quando ele deveria se esforçar? Quando ele deveria abrir mão? No entanto, através da experiência ele logo pegou o jeito da coisa. Parecia que quanto mais fundo ele penetrava no túnel, mais ele simplesmente devia ficar quieto e não fazer nada. Era como andar com patins descendo uma ladeira íngreme - ao relaxar ele avançava, mas ao se esforçar e ficar tenso ele caía.</p>

<p class=Normal>Em sétimo lugar, a atenção plena correta seria crucial. Ele tinha que manter firme atenção dentro do túnel. Sonhar acordado ou cochilar seria fatal evitando avançar mais fundo no túnel. Ele tinha que manter a sua atenção em um dos quatro indicadores direcionando-o para a saída do túnel - corpo, sensações, mente e objetos mentais. Atenção também significava que ele teria que aprender rapidamente com os seus erros e repeti-los com menos freqüência.</p>

<p class=Normal>Oitavo, a quietude correta foi o mais importante de todos. Ele percebeu que quanto mais avançava ao longo do túnel, mais quieto ele tinha que estar de modo a manter o equilíbrio. Seu corpo tinha que ficar sentado como uma estátua por horas, mas isso não foi tão difícil. A parte difícil foi manter a sua mente quieta, tão quieta que o guarda da prisão, Voli, não teria como saber o que Mo pretendia. Sempre que Mo perdia essa quietude, Voli, o guarda tirânico da prisão, apareceria e agarrava Mo de volta para a prisão.</p>

<p class=Normal>A primeira vez que Mo explorou o túnel, ele não foi muito longe, mas foi uma boa experiência. Quanto mais fundo ele ia, mais feliz ele se sentia. Logo Mo tinha aperfeiçoado as primeiras instruções para percorrer o caminho - ele tinha entendimento, pensamento, linguagem, ação e modo de vida corretos. Além disso, ele sabia como equilibrar o esforço, estava extremamente atento e focado, e ele aprendeu como ficar realmente muito quieto.</p>

<p class=Normal>Logo, Mo viu a luz no fim do túnel, aquilo que o livro guia chamava de nimitta. Depois, ele passou pelas impressionantes e gloriosas cavernas chamadas os quatro jhanas. Então, finalmente, Uau! Ele chegou ao fim do túnel. Nirvana! Ele estava por fim fora da prisão. Liberdade. Bem-aventurança. Paz! Ele recebeu um novo nome. Conhecido como Mo Ha ele passou a se chamar Harry Hant.</p>

