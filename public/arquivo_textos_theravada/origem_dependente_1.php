<p class=Normal><b><a name=T1></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. Uma
visão geral da Origem Dependente<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O princípio
da Origem Dependente é um dos ensinamentos Budistas mais importantes e único.
Em muitos trechos do Cânone em Pali, ele foi descrito pelo Buda como uma lei da
natureza, uma verdade fundamental que existe independente do surgimento de
seres iluminados:<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Havendo ou não o surgimento de Tathagatas essa propriedade se mantém –
essa regularidade do Dhamma, essa ordenação do Dhamma, essa condicionalidade
isto/aquilo.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“O Tathagata desperta de forma direta para isso, penetra isso.
Despertando de forma direta e penetrando isso, ele o explica, ensina, proclama,
estabelece, revela, analisa, elucida. E ele diz,<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>‘Vejam, bhikkhus, do nascimento como condição, o envelhecimento e morte
[surgem].<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Portanto, bhikkhus, a realidade disso, a infalibilidade, o que não é de
outra forma, a condicionalidade isto/aquilo: a isto se chama origem
dependente.”[SN.II.25](<a href="../sutta/SNXII.20.php">SN.XII.20</a>)<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os excertos
a seguir indicam a importância que o Buda atribuía ao princípio da Origem
Dependente:<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Quem vê a origem dependente vê o Dhamma; quem vê o Dhamma vê a origem
dependente.” [MN.I.191](<a href="../sutta/MN28.php">MN 28</a>)<o:p></o:p></span></p>

<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-language:PT-BR'>* * *<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Ao invés disso, bhikkhus, o nobre discípulo bem instruído tem conhecimento disso que é 
independente dos outros: ‘Quando não existe isso, aquilo não existe; com a cessação disso, aquilo cessa. 
Quando não há ignorância, as formações volitivas não surgem. Quando não há formações volitivas, a consciência 
não surge. Quando não há consciência, a mentalidade-materialidade (nome e forma) não surge. Quando não há nascimento, o 
envelhecimento e morte não surgem.’ Ele compreende assim : ‘Desse modo o mundo cessa.’”<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Bhikkhus, quando um nobre discípulo assim compreende como na verdade é a origem e a cessação 
do mundo, ele é chamado um nobre discípulo com o entendimento perfeito, com a visão perfeita; ele realizou o 
verdadeiro Dhamma, ele vê esse verdadeiro Dhamma, ele possui o conhecimento de um treinando, que entrou na 
correnteza do Dhamma, um nobre com a sabedoria penetrante que está às portas do Imortal.” [SN.II.79] 
(<a href="../sutta/SNXII.49.php">SN XII.49</a>)<o:p></o:p></span></p>


<p class=Normal align=center style='margin-left:.75in;text-align:center'><span
lang=PT-BR style='mso-ansi-language:PT-BR'>* * *<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Bhikkhus, aqueles contemplativos ou brâmanes que não compreendem o
envelhecimento e morte, a sua origem, a sua cessação, e o caminho que conduz à
sua cessação; que não compreendem o nascimento ... ser/existir ... apego ...
desejo ... sensação ... contato ... seis bases dos sentidos ....
mentalidade-materialidade (nome e forma) ... consciência ... formações volitivas, a sua origem, a sua
cessação, e o caminho que conduz à sua cessação: esses eu não considero serem
contemplativos dentre contemplativos ou brâmanes dentre brâmanes, e esses
veneráveis não irão, realizando por si mesmos através do conhecimento direto,
nesta mesma vida entrar e permanecer no objetivo da vida contemplativa ou no
objetivo da vida brâmane. [<a href="../sutta/SNXII.13.php">SN.XII.13</a>]<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Nesta
conversa com o Venerável Ananda, o Buda o previne para não subestimar a
profundidade do princípio da Origem Dependente:<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“É maravilhoso e admirável, venerável senhor, que essa origem dependente seja 
tão profunda e difícil de ser vista, no entanto para mim ela parece tão simples, clara como a luz do dia”<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Não diga isso, Ananda, não diga isso. Esta origem dependente é um
ensinamento profundo, difícil de ser visto. É por não entender, não compreender e não penetrar 
de forma completa este ensinamento que os seres ficam confusos como um novelo embaraçado, 
como uma corda embolada cheia de nós, como juncos enredados, e não conseguem escapar da 
transmigração, dos planos de miséria, dos destinos ruins, dos mundos inferiores.&quot; [D.ll.92](<a href="../sutta/DN15.php">DN15</a>)<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quem já
estudou a vida do Buda poderá se lembrar das suas ponderações pouco depois da
Iluminação, quando ele ainda não havia começado e expor os ensinamentos. Naquela
ocasião, o Buda relutava em ensinar, tal como relatado nas Escrituras:<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Eu pensei: ‘Este Dhamma que eu alcancei é profundo, difícil de ver e
difícil de compreender, pacífico e sublime, que não pode ser alcançado através
do mero raciocínio, sutil, para ser experimentado pelos sábios.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Mas, esta geração se delicia com a adesão, está excitada com a adesão,
desfruta da adesão. <a name=R9></a>É difícil para uma geração como esta ver
esta verdade, isto é, a condicionalidade isto/aquilo e a origem dependente. E
também é difícil de ver esta verdade, isto é, o cessar de todas as formações, o
abandono de todas aquisições, o fim do desejo, desapego, cessação, Nibbana. Se
eu fosse ensinar o Dhamma, os outros não me entenderiam e isso seria fatigante,
problemático para mim .’.’” [Vin,I.4; MN.I.167] (<a href="../sutta/MN26.php">MN
26</a>)<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Esse trecho
menciona dois ensinamentos, o princípio da Origem Dependente e Nibbana,
enfatizando tanto a sua profundidade como também a sua importância dentro da
iluminação do Buda e dos seus ensinamentos.<o:p></o:p></span></p>

<p class=Normal><b><a name=T11></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>Tipos de
Origem Dependente encontrados nas escrituras<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>As
referências textuais que lidam com o princípio da Origem Dependente podem ser
divididas em duas categorias principais. Primeiro, aquelas que descrevem o
princípio geral e segundo, aquelas que especificam os fatores constituintes
ajuntados numa cadeia. O primeiro formato é com freqüência usado como um esboço
geral precedendo o último. O último, encontrado com mais freqüência, é na maior
parte das vezes expresso por si só.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A última descrição
pode ser considerada como a manifestação prática do princípio da Origem
Dependente, demonstrando como o processo natural segue o princípio geral.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Cada uma
dessas duas categorias principais pode além disso ser dividida em dois membros,
o primeiro mostrando o processo de origem, o segundo, o processo de cessação. O
primeiro membro, mostrando o processo de origem, é chamado de <i>samudayavara</i>.
É a seqüência no seu modo para diante e corresponde à segunda das Quatro Nobres
Verdades, a causa do sofrimento (<i>dukkha samudaya</i>). O segundo membro,
mostrando o processo de cessação, é chamado <i>nirodhavara</i>. É a seqüência
no seu modo reverso e corresponde à terceira Nobre Verdade, a cessação do
sofrimento (<i>dukkha nirodha</i>).<o:p></o:p></span></p>

<p class=Normal><b><a name=T11a></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>a. O
princípio geral<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
essência, este princípio corresponde àquilo que em Pali é conhecido como <i>idappaccayata</i>,
o princípio da condicionalidade.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>A. <i>Imasmim sati idam hoti:</i> <span style='mso-tab-count:2'>               </span>Quando
existe isso, aquilo existe.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><i>Imasuppada idam upajjati:</span></i><span
lang=PT-BR style='mso-ansi-language:PT-BR'> <span style='mso-tab-count:2'>               </span>Com
o surgimento disso, aquilo surge.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>B. <i>Imasmim asati idam na hoti:</i>
<span style='mso-tab-count:1'>         </span>Quando não existe isso, aquilo
também não existe.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><i>Imassa nirodha idam nirujjhati:</i><span
style='mso-spacerun:yes'>        </span>Com a cessação disto, aquilo cessa.
 <o:p></o:p></span></p>

<p class=Normal><b><a name=T11b></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>b. O
princípio em funcionamento<o:p></o:p></span></b></p>

<p class=Normal style='margin-left:.75in'> <span
style='mso-tab-count:1'>       </span><i>Avijja-paccaya sankhara</i><br>

Da ignorância como condição, as formações volitivas[surgem]<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><i>Sankhara-paccaya viññanam</i></span><br>

Das formações volitivas como condição, a consciência.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><i>Viññana-paccaya namarupam</span></i><br>

Da consciência como condição, a mentalidade-materialidade (nome e forma).<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><i>Namarupa-paccaya salayatanam</span></i><span
lang=PT-BR style='mso-ansi-language:PT-BR'><br>

Da mentalidade-materialidade (nome e forma) como condição, as seis bases dos sentidos.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><i>Salayatana-paccaya phasso</span></i><span
lang=PT-BR style='mso-ansi-language:PT-BR'><o:p></o:p></span><br>

Das seis bases dos sentidos como condição, o contato.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><i>Phassa-paccaya vedana</span></i><span
lang=PT-BR style='mso-ansi-language:PT-BR'><o:p></o:p></span><br>

Do contato como condição, a sensação<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><i>Vedana-paccaya tanha</span></i><span
lang=PT-BR style='mso-ansi-language:PT-BR'><o:p></o:p></span><br>

Da sensação como condição, o desejo.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><i>Tanha-paccaya upadanam</span></i><br>

Do desejo como condição, o apego.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><i>Upadana-paccaya bhavo</span></i><span
lang=PT-BR style='mso-ansi-language:PT-BR'><o:p></o:p></span><br>

Do apego como condição, o ser/existir.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><i>Bhava-paccaya jati</span></i><span lang=PT-BR
style='mso-ansi-language:PT-BR'><o:p></o:p></span><br>

Do ser/existir como condição, o nascimento.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><i>Jati-paccaya jaramaranam</span></i><span
lang=PT-BR style='mso-ansi-language:PT-BR'><o:p></o:p></span><br>

Do nascimento como condição, então o envelhecimento e morte, <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><i>Soka-parideva-dukkha-domanassupayasa
sambhavan'ti</span></i><span lang=PT-BR style='mso-ansi-language:PT-BR'><o:p></o:p></span><br>

tristeza, lamentação, dor, angústia e desespero surgem.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><i>Evametassa kevalassa dukkhakkhandhassa samudayo
hoti</span></i><span lang=PT-BR style='mso-ansi-language:PT-BR'><o:p></o:p></span><br>

Essa é a origem de toda essa massa de sofrimento.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><i>Avijjaya tveva asesa-viraga nirodha
sankhara-nirodho</i><o:p></o:p></span><br>

Do desaparecimento e cessação sem deixar vestígios dessa mesma
ignorância, cessam as formações volitivas.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><i>Sankhara-nirodha viññana-nirodho</span></i><br>

Da cessação das formações volitivas, cessa a consciência.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><i>Viññana-nirodha namarupa-nirodho</span></i><br>

Da cessação da consciência, cessa a mentalidade-materialidade (nome e forma).<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><i>Namarupa-nirodha salayatana-nirodho</span></i><br>

Da cessação da mentalidade-materialidade (nome e forma), cessam as seis bases dos
sentidos.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><i>Salayatana-nirodha phassa-nirodho</span></i><br>

Da cessação das seis bases dos sentidos, cessa o contato.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><i>Phassa-nirodha vedana-nirodho</span></i><br>

Da cessação do contato, cessa a sensação.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><i>Vedana-nirodha tanha-nirodho</span></i><br>

Da cessação da sensação, cessa o desejo.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><i>Tanha-nirodha upadana-nirodho</span></i><br>

Da cessação do desejo, cessa o apego.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><i>Upadana-nirodha bhava-nirodho</span></i><br>

Da cessação do apego, cessa o ser/existir.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><i>Bhava-nirodha jati-nirodho</span></i><br>

Da cessação do ser/existir, cessa o
nascimento.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><i>Jati-nirodha jaramaranam</span></i><br>

Da cessação do nascimento, então, o envelhecimento e morte, <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><i>Soka-parideva-dukkha-domanassupayasa nirujjhan 'ti</span></i><br>

tristeza, lamentação, dor, angústia e desespero cessam.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><i>Evametassa kevalassa dukkhakkhandhassa nirodho hoti</span></i><br>

Essa é a cessação de toda essa
massa de sofrimento. [Vin.I.I-3; SN.II.l,65] (<a href="../sutta/SNXII.2.php">SN
XII.2</a>) </span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Observe que
este formato trata o princípio da Origem Dependente como o processo do
surgimento e cessação do sofrimento. Esse é o fraseado mais comumente
encontrado nos textos. Em alguns lugares, ele é apresentado como o surgimento e
a cessação do mundo, usando as palavras em Pali, <i>ayam kho bhikkhave lokassa
samudayo</i> – “Essa, bhikkhus, é a origem do mundo,” e <i>ayam kho bhikkhave
lokassa atthangamo</i> – “Esse, bhikkhus, é o fim do mundo” [SN.II.73] (<a
href="../sutta/SNXII.44.php">SN.XII.44</a>); ou <i>emamayam loko samudayati</i>
– “Assim este mundo surge;” e <i>emamayam loko nirujjhati</i> – “Assim este
mundo cessa” [SN.II.78] (SN XII.49). Ambos fraseados têm na verdade o mesmo significado, o que
ficará claro uma vez que os termos sejam definidos.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Nos textos
do Abhidhamma e nos Comentários o princípio da Origem Dependente também é
conhecido como <i>paccayakara</i>, referindo-se à natureza interdependente das
coisas.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O formato
ampliado, mostrado acima, contém doze fatores ligados de forma interdependente
como uma cadeia. Não possui início nem fim. A colocação da ignorância no início
não indica que esta seja a Primeira Causa, ou Gênesis, de todas as coisas. A
ignorância é colocada no início apenas para efeito de visibilidade, detendo o
ciclo e estabelecendo um ponto de início no ponto considerado mais prático.
Somos na verdade avisados a não tomar a ignorância como a Primeira Causa com a
seguinte descrição do surgimento condicionado da ignorância - <i>Asava-samudaya
avijja-samudayo, asava-nirodha avijja-nirodho</i> – a ignorância surge com o
surgimento das impurezas e cessa com a cessação destas. [MN.I.55](<a
href="../sutta/MN10.php">MN 10</a>)<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os doze
elos do formato padrão do princípio da Origem Dependente são contados apenas da
ignorância até o envelhecimento e morte. Quanto à ‘tristeza, lamentação, dor,
angústia e desespero’, esses são na verdade subprodutos do envelhecimento e
morte, para quem possua impurezas (<i>asava</i>) e contaminações <i>(kilesas)</i>,
que se tornam o ‘fertilizante’ para o futuro surgimento de impurezas e por
conseqüência da ignorância, que mais uma vez gira o ciclo.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O Buda nem
sempre descrevia o ciclo de Origem Dependente em um formato fixo (do começo ao
fim). O formato ampliado era empregado nos casos em que ele estava explicando o
princípio de forma geral, mas quando ele estava abordando um problema em
particular, com freqüência ele aplicava o formato em ordem reversa, assim:
envelhecimento e morte &lt;= nascimento &lt;= ser/existir &lt;= apego &lt;=
desejo &lt;= sensação &lt;= contato &lt;= seis bases dos sentidos &lt;=
mentalidade-materialidade (nome e forma) &lt;= consciência &lt;= formações &lt;= ignorância
[SN.II.5-11 ,81] (<a href="../sutta/SNXII.20.php">SN.XII.20</a>, <a
href="../sutta/SNXII.65.php">XII.65</a>). Em outras descrições ele poderia
começar num dos fatores intermediários, dependendo do problema em questão. Por
exemplo, ele poderia começar com o nascimento (<i>jati</i>) [SN.II.52](<a
href="../sutta/SNXII.68.php">SN.XII.68</a>), sensação (<i>vedana</i>)
[MN.I.266] (<a href="../sutta/MN38.php">MN 38</a>), ou<span
style='mso-spacerun:yes'>  </span>consciência (<i>viññana</i>) [SN.II.77] (<a
href="../sutta/SNXII.44.php">SN.XII.44</a>), seguindo os passos para diante até
envelhecimento e morte (<i>jaramarana</i>), ou regressando para chegar na
ignorância (<i>avijja</i>). Ou ele poderia começar com um fator totalmente
distinto dos doze elos, que depois era inserido na cadeia da Origem Dependente.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Um outro
ponto digno de menção é que a origem dependente desses elos não tem o mesmo
significado de ‘ser causado por’ como tal. Os fatores determinantes que fazem
com que uma árvore cresça, por exemplo, incluem não somente a semente, mas
também o solo, a umidade, o fertilizante, a temperatura do ar e assim por
diante. Esses são todos os fatores ‘determinantes’. Além disso, ser um fator
determinante não necessariamente implica uma ordem seqüencial no tempo. Por
exemplo, no caso da árvore, os vários fatores determinantes, como a umidade, a
temperatura, o solo e assim por diante, precisam existir em conjunto, não em
seqüência, para que a árvore se beneficie. Além disso, alguns fatores
determinantes são interdependentes, cada um condicionando a existência do
outro, como por exemplo, um ovo é uma condição para uma galinha, enquanto que
uma galinha é uma condição para um ovo.<o:p></o:p></span></p>


