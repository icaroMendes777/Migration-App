<p class=Normal><b><a name=Introducao></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>Introdução<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O
ensinamento da interdependência causal é o princípio Budista mais importante.
Ele descreve a lei da natureza existente como o curso natural das coisas. O
Buda não era um emissário de mandamentos divinos, mas o descobridor desse
princípio da natureza e o proclamador dessa verdade para o mundo.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A
progressão de causas e condições é a realidade que se aplica a todas as coisas
desde o meio ambiente natural, que é uma condição física, externa, até os eventos
das sociedades humanas, os princípios éticos, os eventos da vida e a felicidade
e sofrimento que se manifestam nas nossas mentes. Esses sistemas de relação
causal são parte da mesma verdade da natureza. Nossa felicidade dentro desse
sistema natural depende de termos o conhecimento de como ele funciona e de
agirmos corretamente dentro dele ao abordarmos os problemas nos níveis pessoal,
social e ambiental. Pelo fato de todas as coisas estarem interconectadas e
afetarem umas às outras, o êxito em lidar com o mundo consiste em criar
harmonia dentro dele.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Costuma-se
dizer que as ciências, que evoluíram junto com a civilização humana e que
influenciam as nossas vidas de maneira tão profunda na atualidade, estão
baseadas na razão e na racionalidade. O seu repositório de conhecimentos foi
acumulado através da interação com essas leis da condicionalidade. Mas a busca
humana pelo conhecimento nos campos da ciência moderna possui três
características notáveis: primeiro, a busca pelo conhecimento nessas ciências e
a aplicação desse conhecimento estão separadas em categorias distintas. Cada
ramo da ciência é distinto dos demais. Segundo, os seres humanos nas
civilizações atuais acreditam que a lei da condicionalidade se aplica apenas ao
mundo físico, não ao mundo mental ou aos valores abstratos, como por exemplo a
ética. Isso pode ser observado até mesmo no estudo da psicologia, que tende a
olhar para o processo de causa e efeito apenas em relação aos fenômenos
físicos. Terceiro, a aplicação do conhecimento científico (das leis da
condicionalidade) é dirigida exclusivamente para satisfazer interesses
egoístas. Nosso relacionamento com o meio ambiente, por exemplo, está centrado
no esforço de extrair dele tantos recursos quanto possível, com pouca ou
nenhuma consideração para com as conseqüências.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Por trás de
tudo isso, tendemos a interpretar conceitos como a felicidade, liberdade,
direitos, independência e paz, de modo a preservar interesses próprios e abusar
dos interesses dos outros. Mesmo quando o controle sobre outras pessoas passa a
ser visto como uma ação censurável, essa tendência agressiva passa então a ser
dirigida em outras direções, tal como o meio ambiente. Agora que estamos
começando a compreender que é impossível na realidade controlar outras pessoas
ou outras coisas, o único sentido que resta na vida é preservar os interesses
próprios e proteger os direitos territoriais. Ao vivermos dessa maneira, com
essa compreensão falha e crenças equivocadas, colocamos o meio ambiente em
desequilíbrio, a sociedade entra em convulsão e a vida humana fica
desorientada, tanto física como mentalmente. O mundo parece estar repleto de
conflito e sofrimento.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Todas as
facetas da natureza – o mundo físico e o mundo humano, o mundo das condições (<i>dhamma</i>)
e o mundo das ações (<i>kamma</i>), o mundo material e o mundo mental – estão
conectados e inter-relacionados, eles não podem ser separados. A desordem e a
anormalidade num setor irão afetar os outros setores. Se quisermos viver em
paz, temos que aprender a viver em harmonia com todas as esferas da natureza,
tanto internas como externas, o individual e o social, o físico e o mental, o
material e o imaterial.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Para criar
a verdadeira felicidade é da maior importância que não somente reflitamos sobre
a inter-relação de todas as coisas na natureza, mas que também nos vejamos com
clareza como um sistema de relações causais, como parte da natureza,
tornando-nos conscientes primeiro dos fatores mentais internos, em seguida dos
fatores das nossas experiências
de vida, da sociedade e por fim do mundo à nossa volta. É por isso que em todos
os sistemas de relação causal baseados na lei “Com o surgimento disso, aquilo
surge; Com a cessação disto, aquilo cessa,” os ensinamentos Budistas principiam
e sempre enfatizam os fatores envolvidos na criação do sofrimento situados na
consciência do indivíduo – “porque há ignorância, surgem as formações.” Uma vez
que esse sistema de relações causais seja compreendido no nível interno,
estaremos então em posição de ver as conexões entre esses fatores internos e as
relações causais na sociedade e no meio ambiente natural. Essa é a abordagem
adotada neste livro.<o:p></o:p></span></p>


