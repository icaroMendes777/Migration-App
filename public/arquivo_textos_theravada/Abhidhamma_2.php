<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O segundo
capítulo do <i>Abhidhammattha Sangaha</i> é dedicado à análise de <i>cetasika</i>,
fatores mentais, a segunda das quatro realidades últimas. Os <i>cetasikas </i>são
fenômenos mentais que ocorrem em conjunção imediata com <i>citta </i>ou
consciência e assistem <i>citta </i>realizando tarefas mais específicas no ato
completo da cognição. Os fatores mentais não surgem sem <i>citta, </i>nem <i>citta
</i>pode surgir segregada dos fatores mentais. Mas embora ambos sejam
funcionalmente interdependentes, <i>citta </i>é considerada como fator primário
porque os fatores mentais ajudam na cognição do objeto na dependência de<i>
citta, </i>que é o principal elemento cognitivo. A relação entre <i>citta </i>e
os <i>cetasikas </i>é comparada com aquela de um rei e o seu séquito. Embora
alguém diga “o rei está vindo,” o rei não vem só, ele sempre vem acompanhado do
seu séquito. De modo semelhante, sempre que uma <i>citta </i>surge, ela nunca
surge só, pois está sempre acompanhada pelo seu séquito de <i>cetasikas</i>.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os <i>cetasikas
</i>compartem quatro características em comum:<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>1. surgem junto com a consciência<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>2. cessam junto com a consciência<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>3. possuem o mesmo objeto que a
consciência<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>4. possuem a mesma base que a
consciência<o:p></o:p></span></p>


