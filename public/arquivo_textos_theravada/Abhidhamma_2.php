<p class=Normal><b> 1. Variáveis sob o ponto de vista ético (<i>aññasamanacetasika</i>) <o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>As
primeiras duas categorias de fatores mentais – os sete universais e os seis
ocasionais – são denominados <i>aññasamana </i>que é interpretado como
“variável sob o ponto de vista ético.” Literalmente, a expressão quer dizer
“comum aos demais.” As <i>cittas</i> não-belas são chamadas de “outras” (<i>añña</i>)
em relação às <i>cittas</i> belas, e as <i>cittas</i> belas são chamadas de
outras em relação às <i>cittas</i> não-belas. Os treze <i>cetasikas</i> das
duas primeiras categorias são comuns, (<i>samana</i>), a ambos os <i>cetasikas,</i>
belos e não-belos e assumem a qualidade ética transmitida para a <i>citta</i>
pelos demais <i>cetasikas</i>, particularmente pelas raízes, (<i>hetu</i>),
associadas. <o:p></o:p></span></p>

<p class=Normal><b> 1.1 Universais (<i>sabbacittasadharana</i>)<span style="mso-spacerun:
yes">  </span><o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Esses sete
universais são os <i>cetasikas</i> comuns a todas as consciências. Estes
fatores desempenham as funções cognitivas mais rudimentares e essenciais, sem
as quais a consciência de um objeto seria absolutamente impossível.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(1)
Contato, (<i>phassa</i>): A palavra <i>phassa</i> é derivada do verbo <i>phusati</i>
que significa “tocar,” mas o contato não deve ser compreendido como o mero
impacto do objeto na faculdade do sentido. Ele é na verdade o fator mental
através do qual a consciência mentalmente “toca” o objeto que tenha surgido, dessa
forma dando início a todo o evento cognitivo.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(2)
Sensação, (<i>vedana</i>): Sensação é o fator mental que sente o objeto: é o
tom emocional com o qual o objeto é experimentado. Assim, <i>vedana</i> é um
tipo de julgamento, não sob a forma intelectual mas sob um critério subjetivo
de aceitação, rejeição ou indiferença. A natureza da sensação em si não pode
ser definida porque toda definição tem que ser dada intelectualmente e o
intelecto e as sensações não são comensuráveis. Dessa forma as sensações
possuem um certo caráter autônomo, indicado pelo símile de um rei que devido
à<span style="mso-spacerun: yes">  </span>virtude da sua nobreza, habilidade e
maestria, saboreia e desfruta de toda a refeição preparada por outros, e já que
cada cozinheiro esteve apenas parcialmente envolvido na preparação da refeição,
eles não são capazes de se dar conta disso. Ou seja, <i>vedana</i> desfruta o
sabor do objeto de forma plena e completa. A palavra em Pali <i>vedana</i> não
significa emoção, (que é um fenômeno complexo envolvendo vários fatores mentais
concomitantes), mas a qualidade emocional básica de uma experiência, que pode
ser prazerosa, dolorosa ou neutra.<span style="mso-spacerun: yes">  </span><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(3)
Percepção, (<i>sañña</i>): O sufixo “<i>ñña</i>” pode ser interpretado como
conhecimento enquanto que “<i>sa</i>” significa juntar/reunir ou seja “juntar
aquilo que conhecemos.” A característica de <i>sañña</i> é perceber as
qualidades de um objeto. A sua função é fazer uma marca ou sinal como condição para
perceber, numa outra ocasião, que “é a mesma coisa,” ou, a sua função é
reconhecer aquilo já que havia sido percebido antes. Ela se manifesta ao interpretar
o objeto através das marcas ou sinais que foram compreendidos. O seu
procedimento se compara ao do carpinteiro que reconhece os tipos de madeira
pelas distintas marcas que fez nelas.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(4) Volição
ou intenção, (<i>cetana</i>): <i>Cetana</i>, que possui a mesma raiz que <i>citta</i>,
é o fator mental que está relacionado com a realização de um objetivo ou de um
determinado resultado. Sua função é estimular ou dirigir os demais <i>cetasikas</i>
numa certa direção. Pode ser<span style="mso-spacerun: yes">  </span>comparado a
um comandante numa batalha, que ao começar a lutar, estimula os seus comandados
a lutarem também, assim ao realizar a sua tarefa em relação ao objeto, <i>cetana</i>
faz com que os <i>cetasikas</i> associados também cumpram o seu papel. Volição
ou intenção é o fator mental mais significativo na geração de <i>kamma</i>.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(5)
Unicidade num único ponto, (<i>ekaggata</i>): a unificação da mente no objeto.
Embora este fator ganhe proeminência nos<i> jhanas</i>, onde ele funciona como
um fator de <i>jhana</i>, o Abhidhamma ensina que o germe da capacidade para a
unificação da mente está presente em todos os tipos de consciência, até mesmo
nos mais rudimentares. A sua função é fixar a mente no seu objeto. A sua
característica é a não-distração.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(6)
Faculdade vital, (<i>jivitindriya</i>): Existem dois tipos de faculdade vital,
a mental, que vitaliza ou mantém os fenômenos mentais associados, e a física,
que vitaliza ou mantém os fenômenos materiais. A sua função é fazer com que os
fenômenos ocorram.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(7)
Atenção, (<i>manasikara</i>): A atenção é o fator mental que tem a responsabilidade
de advertir a mente para o objeto, fazendo com que ele se torne presente para a
consciência. A sua característica é a condução dos fatores mentais associados
para o objeto. A sua função é unir os fatores mentais associados ao objeto. A
atenção é como o leme de um barco, que o conduz ao seu destino, ou como um
condutor de uma carruagem que conduz os cavalos, (fatores mentais ), ao seu
destino, (o objeto). <i>Manasikara</i> deve ser diferenciado de <i>vitakka</i>:
enquanto que o primeiro dirige os seus concomitantes em direção ao objeto, o
último os aplica ao objeto. <i>Manasikara</i> é um fator cognitivo
indispensável presente em todos os estados de consciência; <i>vitakka</i> é um
fator especializado que não é indispensável para a cognição.<o:p></o:p></span></p>


<p class=Normal><b> 1.2 Ocasionais (<i>pakinnaka</i>)<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os seis <i>cetasikas</i>
deste grupo são semelhantes aos universais por serem fatores variáveis sob o
ponto de vista ético, que assumem a qualidade moral da <i>citta,</i>
determinada por outros fatores concomintantes. Eles diferem dos universais por
serem encontrados apenas em tipos particulares de consciência, não em todas.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(8)
Pensamento aplicado, (<i>vitakka</i>): Nos suttas, <i>vitakka</i> é com
freqüência usado como sinônimo para pensamento, mas no Abhidhamma ele é usado
de um modo tecnicamente mais preciso significando o fator mental que aplica a
mente ao objeto. A sua característica é dirigir a mente para o objeto. <i>Vitakka
</i>pode ser comparado a uma pessoa que tem amizade ou é parente do rei e
estabelece uma conexão entre o rei e uma outra pessoa que quer se encontrar com
ele. Quando é cultivado através da concentração, <i>vitakka</i> se torna um dos
fatores de <i>jhana,</i> (inibindo o obstáculo da preguiça e torpor), é a
absorção da mente no objeto. <i>Vitakka</i> também é chamado <i>sankappa</i>, e
<i>sammasankappa</i>, (pensamento correto), é o segundo elemento do Nobre
Caminho Óctuplo.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(9)
Pensamento sustentado, (<i>vicara</i>): A palavra <i>vicara</i> em geral
significa investigação, mas neste caso significa a sustentação da
mente no objeto. <i>Vicara</i> também é um fator de <i>jhana,</i> (inibindo o
obstáculo da dúvida), e tem como característica a pressão contínua sobre o
objeto no sentido de examiná-lo. A sua função é a sustentação dos
fatores mentais associados ao objeto. Os comentários empregam vários símiles
para mostrar a relação entre <i>vitakka</i> e <i>vicara</i>. <i>Vitakka</i> é
como uma abelha mergulhando em direção a uma flor, enquanto que <i>vicara</i> é
a abelha zumbindo em volta da flor. <i>Vitakka</i> é como a mão que segura uma
peça de metal manchada, <i>vicara</i> é como a outra mão que limpa a peça. Ou <i>vitakka
</i>é como a ponta de um compasso fixa no meio da folha de papel enquanto que <i>vicara
</i>é a ponta que desenha o círculo.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(10)
Decisão, (<i>adhimokkha</i>): A palavra <i>adhimokkha</i> significa
literalmente a deliberação da mente em relação ao objeto. Assim foi
interpretada como decisão ou resolução. Possui a característica da decisão. É
comparada a uma pilastra de pedra devido à sua decisão inabalável em relação ao
objeto.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(11)
Energia, (<i>viryia</i>): <i>Viriya</i> é o estado ou a ação de alguém que
tenha vigor. A energia tem a natureza da perseverança. A energia suporta e
mantém os demais fatores associados e não permite que eles esmoreçam. Da mesma
forma como pilastras novas de madeira não permitem que a casa desabe ou como
reforços numa batalha permitem que o exército do rei derrote o inimigo.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(12)
Êxtase, (<i>piti</i>): <i>Piti </i>é derivado do verbo <i>pinayati</i>
significando “refrescar” e pode ser explicado como o deleite ou prazer em
relação ao objeto. <i>Piti </i>se diferencia de <i>vedana </i>por ser uma
sensação agradável intensa com distintos estímulos no corpo físico. A tradução
mais freqüente é êxtase, que é uma qualificação adequada quando ele é um dos
fatores de <i>jhana,</i> mas que pode não ser suficientemente ampla para
capturar todas as suas nuances. Os comentários distinguem cinco graus de <i>piti</i>
que surgem ao desenvolver a concentração: êxtase menor, êxtase momentâneo,
êxtase como uma ducha, êxtase que causa elação e êxtase impregnante. O êxtase
menor pode fazer subir os pêlos do corpo. Êxtase momentâneo é igual a lampejos
de relâmpagos. Êxtase como ducha atravessa o corpo como ondas do oceano. Êxtase
que causa elação pode fazer o corpo levitar. E o êxtase impregnante que inunda
todo o corpo como numa inundação que enche uma caverna. Este último é <i>piti</i>
tal como se apresenta em <i>jhana</i>. Como um fator de <i>jhana</i>, <i>piti</i>
inibe o obstáculo da má vontade.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(13)
Aspiração, (<i>chanda</i>): <i>Chanda</i> significa o desejo de agir, isto é,
realizar uma ação ou lograr um resultado. Aqui foi traduzido como aspiração
para diferenciar do desejo, (<i>tanha</i>), e cobiça, (<i>lobha</i>). Estes
dois últimos são sempre insalubres, (inábeis), mas <i>chanda</i> é um fator
variável sob o ponto de vista ético, que quando combinado com concomitantes
saudáveis pode funcionar como o desejo virtuoso de lograr um objetivo digno.<o:p></o:p></span></p>


<p class=Normal><b> 2. Fatores Insalubres (<i>akusalacetasika</i>)<o:p></o:p></span></b></p>


<p class=Normal><b> 2.1 Universais Insalubres<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(14)
Delusão, (<i>moha</i>): Moha é um sinônimo de <i>avijja</i>, ignorância. A sua
característica é a cegueira mental ou o desconhecimento (<i>aññana</i>). A sua
função é não penetrar ou encobrir a real natureza do objeto. A sua manifestação
é a ausência do entendimento correto ou como obscuridade mental. A sua causa
mais próxima é a atenção sem sabedoria, (<i>ayoniso manasikara</i>). <i>Moha</i>
deve ser vista como a raiz de tudo aquilo que é insalubre.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(15, 16)
Ausência de vergonha moral ou de cometer transgressões, (<i>ahirika</i>),
e ausência de temor moral ou de cometer transgressões, (<i>anottappa</i>): 
A característica da ausência de vergonha de cometer
transgressões é a falta de auto-respeito, aquilo que nos refreia de cometer atos que colocariam 
em risco o respeito que temos por nós mesmos; a característica da ausência de temor de cometer transgressões, é a
falta de temor dos resultados de kamma desfavoráveis e da crítica e da punição imposta por outros.
Ambas possuem a função de praticar ações insalubres. A sua causa mais próxima é
a falta de respeito por si mesmo e pelos outros, respectivamente.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(17)
Inquietação, (<i>uddhacca</i>): Inquietação, (ou agitação), possui como
característica a intranqüilidade, como a água agitada pelo vento. A sua função
é fazer com que a mente fique desequilibrada.<o:p></o:p></span></p>

<p class=Normal><b> 2.2 Ocasionais Insalubres<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(18)
Cobiça, (<i>lobha</i>): Cobiça, a primeira raiz insalubre, abrange todas as
graduações de desejo egoísta, anseio, ligação sentimental e apego. A sua
característica é agarrar-se ao objeto. A sua função é grudar, como a carne que
gruda numa frigideira quente. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(19)
Entendimento Incorreto, (<i>ditthi</i>): <i>Ditthi</i> neste caso significa ver
de modo incorreto. A sua característica é a interpretação das coisas sem
sabedoria. A sua função é pressupor. A sua manifestação é a crença ou
interpretação incorreta.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(20)
Presunção, (<i>mana</i>): Presunção tem a característica da arrogância. A sua
função é a auto-exaltação. A sua manifestação é o orgulho.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(21) Raiva,
(<i>dosa</i>): <i>Dosa</i>, a segunda raiz insalubre, compreende todas as
graduações da aversão, má vontade, raiva, irritação, aborrecimento e
animosidade.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(22)
Inveja, (<i>issa</i>): A inveja tem como característica o ciúme do sucesso dos
outros. A sua função é estar insatisfeito com o sucesso dos outros. Ela se
manifesta como aversão a isso.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(23)
Avareza, (<i>macchariya</i>): A característica da avareza é ocultar o próprio
sucesso. A sua função é não tolerar compartí-lo com os outros.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(24)
Ansiedade, (<i>kukkucca</i>): <i>Kukkucca</i> é a preocupação, remorso,
arrependimento com relação a coisas feitas de modo incorreto e coisas corretas que foram negligenciadas. A sua característica é o
arrependimento. A sua função é causar tristeza em relação ao que foi feito
e ao  que não foi feito. Ela se manifesta como remorso.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(25)
Preguiça, (<i>thina</i>): Preguiça é a indolência ou entorpecimento. A
sua característica é a falta de energia. A sua função é dissipar energia. Ela
se manifesta como o afundamento da mente. A sua causa mais próxima é a atenção
sem sabedoria em relação ao tédio, entorpecimento, etc.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(26)
Torpor, (<i>middha</i>): Torpor é o estado deprimido dos fatores mentais. A sua
função é oprimir. A sua causa mais próxima é a mesma da preguiça.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Preguiça e
torpor sempre ocorrem em conjunto e são opostos à energia. A preguiça é
identificada como a enfermidade da consciência e o torpor como a enfermidade
dos fatores mentais. Como um par, eles constituem um dos cinco obstáculos que é
superado pelo pensamento aplicado, (<i>vitakka</i>).<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(27)
Dúvida, (<i>vicikiccha</i>): Neste caso significa dúvida espiritual, sob a perspectiva
Budista, a incapacidade de depositar confiança no Buda, Dhamma e Sangha e no
treinamento Budista. Ela se manifesta como indecisão e a toma de vários
partidos. A sua causa mais próxima é a atenção sem sabedoria.<o:p></o:p></span></p>

<p class=Normal><b> <a name=belos></a> 3. Fatores Belos (<i>sobhanasadharana</i>)<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os fatores
mentais belos estão subdivididos em quatro grupos, sendo que o primeiro inclui
dezenove <i>cetasikas </i>que estão sempre presentes em todas as consciências
belas. <o:p></o:p></span></p>


<p class=Normal><b> 3.1 Universais Belos<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(28)
Convicção, <i>(saddha)</i>: O primeiro dos <i>cetasikas </i>belos é a convicção,
fé, certeza, reverência, respeito, que possui a característica de depositar fé
ou confiança. A sua função é aclarar, como uma substância que clarifica a água e faz
com que a água barrenta se torne clara; ou colocar-se a caminho, como alguém
que se ponha a caminho para cruzar uma correnteza. Ela se manifesta como não
obscuridade, isto é, a remoção das impurezas da mente e de todos os obstáculos.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(29)
Atenção Plena, <i>(sati)</i>: A palavra <i>sati </i>é derivada de uma raiz que
significa “memória,” mas como fator mental significa presença de espírito,
estar atento ao presente, ao invés da faculdade da memória em relação ao
passado. A sua característica é a não vacilação, isto é, não se afastar do
objeto. A sua função é a ausência de confusão ou não-esquecimento. A sua causa
mais próxima é a percepção intensa ou os quatro fundamentos da atenção plena.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(30, 31)
Vergonha moral ou de cometer transgressões, <i>(hiri), </i>e temor moral ou de  cometer transgressões, <i>(ottappa)</i>:
<i>Hiri</i> equivale à vergonha de cometer transgressões ou o auto-respeito, aquilo que nos refreia de 
cometer atos que colocariam em risco o respeito que temos por nós mesmos; <i>ottappa</i> equivale ao temor de cometer 
transgressões que produzam resultados de kamma desfavoráveis ou o temor da crítica e da punição imposta por outros. 
A sua causa mais próxima é o auto-respeito e o respeito pelos
outros, respectivamente. Esses dois estados são chamados pelo Buda de guardiões
do mundo porque eles protegem o mundo evitando que ele caia na imoralidade
desenfreada.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(32)
Não-cobiça, <i>(alobha)</i>: A não-cobiça tem como característica a ausência de
desejo na mente, ou de não aderência ao objeto, como uma gota d’água numa flor
de lótus. A sua função é não segurar e a sua manifestação é o desapego. Deve
ser compreendido que a não-cobiça não é a mera ausência de cobiça, mas a
presença de qualidades positivas como a generosidade e a renúncia também.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(33)
Não-raiva, <i>(adosa)</i>: A não-raiva tem como característica a ausência de
ferocidade, ou não-oposição. A sua função é remover o aborrecimento ou remover
a febre. A não-raiva compreende qualidades positivas como amor bondade,
gentileza, amizade, etc.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando a
não-raiva aparece como qualidade sublime de amor bondade, <i>(metta), </i>ela
tem como característica a promoção do bem estar dos seres vivos. A sua função é
dar preferência ao bem estar dos outros. A sua manifestação é a remoção da má
vontade. A sua causa mais próxima é ver os seres como passíveis de serem
amados.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(34)
Neutralidade mental, <i>(tatramajjhattata)</i>: É um sinônimo para
equanimidade, <i>(upekkha), </i>não como uma sensação neutra, mas uma atitude
mental de equilíbrio, desapego e imparcialidade.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os próximos
doze <i>cetasikas </i>universais se encaixam dentro de seis pares, cada um
contendo um termo que se aplica ao “corpo mental”, (<i>kaya</i>), e outro que
se aplica à consciência, (<i>citta</i>). Neste contexto o corpo mental é a
coleção dos <i>cetasikas </i>associados, chamados de corpo no sentido de
agregação.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(35, 36)
Tranqüilidade, (<i>passaddhi</i>): Tem como característica acalmar as
perturbações no corpo mental e na consciência. Deve ser considerado como o
oposto das contaminações, inquietação e ansiedade, que causam aflição.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(37, 38)
Leveza, (<i>lahuta</i>): Tem como característica aplacar o peso no corpo mental
e na consciência. Ela se<span style="mso-spacerun: yes">  </span>sua manifesta
como não- indolência. Deve ser considerada como o oposto das contaminações
preguiça e torpor que causam peso.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(39, 40)
Maleabilidade, (<i>muduta</i>): Tem como característica aplacar a rigidez no
corpo mental e na consciência. Ela se manifesta como não-resistência. Deve ser
considerada como o oposto das contaminações das idéias incorretas e presunção,
que criam rigidez.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(41, 42)
Manuseabilidade,<i>(kammaññata)</i>: Tem como característica aplacar a falta de
manuseabilidade no corpo mental e na consciência. Deve ser considerada como o
oposto dos demais obstáculos que criam a falta de manuseabilidade no corpo
mental e na consciência.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(43, 44)
Proficiência, (<i>paguññata</i>): Tem como característica a saúde do corpo
mental e da consciência. A sua manifestação é como a ausência de deficiência.
Deve ser considerada como o oposto à falta de convicção que provoca a
insalubridade do corpo mental e da consciência..<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(45, 46)
Retidão, (<i>ujjukata)</i>: Tem como característica a integridade do corpo
mental e da consciência. Deve ser considerada como o oposto à hipocrisia e à
fraude que criam a desonestidade no corpo mental e na consciência.<o:p></o:p></span></p>


<p class=Normal><b> 3.2 Abstinências <i>(virati)<o:p></o:p></i></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os <i>viratis
</i>são três fatores mentais belos responsáveis pela abstinência deliberada de
conduta imprópria através da linguagem, ação e modo de vida. Na consciência
mundana, os <i>viratis</i> operam apenas no momento da existência da abstenção
de uma conduta imprópria e para a qual tenha surgido uma oportunidade. Quando
uma pessoa se abstém de atos impróprios, sem que tenha surgido uma oportunidade
para praticá-los, este não é um caso de <i>virati, </i>mas de pura conduta
moral (<i>sila</i>).<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(47)
Linguagem Correta, (<i>sammavaca</i>): Abstenção deliberada da linguagem
incorreta: linguagem mentirosa, linguagem maliciosa, linguagem grosseira e
linguagem frívola.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(48) Ação
Correta, (<i>sammamammanta)</i>: Abstenção deliberada da conduta corporal
imprópria: matar, roubar e conduta sexual imprópria.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(49) Modo
de Vida Correto, (<i>samma-ajiva</i>): Abstenção deliberada do modo de vida
incorreto: lidar com venenos, substâncias embriagantes, armas, escravos,
animais para abate.<o:p></o:p></span></p>

<p class=Normal><b> 3.3 Ilimitados (<i>appamanna</i>)<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Existem
quatro atitudes em relação aos seres vivos chamadas de ilimitadas porque elas
se aplicam a todos os seres vivos, e dessa forma possuem uma abrangência
potencial ilimitada. Os quatro estados ilimitados são amor bondade, <i>(metta),
</i>compaixão, <i>(karuna), </i>alegria altruísta, <i>(mudita),</i>e<i> </i>equanimidade,
<i>(upekkha)</i>. Esses quatro também são chamados de moradas divinas, estados
divinos ou <i>brahmaviharas.<o:p></o:p></i></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Embora
sejam reconhecidos quatro estados ilimitados como as atitudes ideais em relação
aos demais seres, apenas dois – compaixão e alegria altruísta – estão incluídos
como <i>cetasikas </i>neste grupo dos ilimitados. Isso se deve ao fato de, como
foi visto, o amor bondade ser uma forma do <i>cetasika adosa, </i>não-raiva, e
a equanimidade ser uma forma do <i>cetasika tatramajjhattata, </i>neutralidade
da mente. Enquanto a não-raiva e a neutralidade da mente – fatores que suportam
o amor bondade e a equanimidade – estão presentes em todas as <i>cittas </i>belas,
os dois fatores do grupo dos ilimitados, (alegria altruísta e compaixão), estão
presentes apenas nas ocasiões em que as suas funções individuais são exercidas.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(50)
Compaixão, <i>(karuna): </i>Tem como característica promover a remoção do
sofrimento dos outros. A sua função é não ser capaz de tolerar o sofrimento dos
outros. Ela se manifesta como não crueldade.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(51)
Alegria altruísta, <i>(mudita): Mudita </i>tem como característica o
contentamento com o sucesso dos outros. A sua função é não ter inveja do
sucesso dos outros. Ela se manifesta como a eliminação da aversão.<o:p></o:p></span></p>


<p class=Normal><b> 3.4 Não-Delusão <i>(amoha)</i><o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(52) A
faculdade da sabedoria:<span style="mso-spacerun: yes">  </span><i>Pañña</i> é
sabedoria ou a compreensão das coisas tal como elas na verdade são. É aqui
chamada de faculdade porque exerce predominância na compreensão das coisas como
elas na verdade são. No Abhidhamma, os três termos – sabedoria, <i>(pañña), </i>conhecimento,
<i>(ñana)</i> e não-delusão, <i>(amoha) </i>– são usados como sinônimos. A
sabedoria tem como característica apreender as coisas de acordo com a sua
característica intrínseca. A sua causa mais próxima é atenção com sabedoria, (<i>yoniso
manasikara).</i><o:p></o:p></span></p>


