<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O objetivo da prática
Budista, nibbana, se diz que é completamente desprovido de causa e exatamente
nisso existe um paradoxo. Se o objetivo é desprovido de causa, como pode um
caminho de prática - que é causal por natureza - produzi-lo? Essa é uma questão
antiga. O <i>Milinda-pañha, </i>um conjunto de diálogos composto no início da
era cristã, relata uma conversa entre o rei Milinda e um monge, Nagasena, no
qual o rei pergunta a Nagasena exatamente sobre isso. Nagasena responde com uma
analogia. O caminho da prática não causa nibbana, ele diz. Ele simplesmente o
leva até lá - tal como um caminho para uma montanha não faz com que a montanha
exista. Ele somente o leva até lá. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A resposta de Nagasena,
embora muito apropriada, na verdade não solucionou o caso dentro da tradição
Budista. Ao longo dos anos muitas escolas de meditação têm ensinado que as
fabricações mentais somente atrapalham o atingimento de um objetivo que é
desprovido de causas e não é fabricado. Somente através do não fazer absolutamente
nada, e dessa forma não fabricando nada na mente, eles dizem, o não fabricado
surgirá. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Esse entendimento é baseado
numa compreensão bastante simplista do que é a realidade fabricada, vendo a
causalidade como linear e totalmente previsível: X causa Y que causa Z e assim
por diante, sem nenhum efeito dando uma volta para condicionar as suas causas e
sem nenhuma possibilidade de usar a causalidade para escapar da teia causal.
Uma das muitas coisas que o Buda descobriu no seu processo de iluminação foi de
que a causalidade não é linear. A experiência no presente é moldada tanto por
ações no presente como por ações do passado. As ações no presente moldam o
presente e o futuro. Os resultados de ações do passado e do presente interagem
continuamente. Assim existe sempre espaço para adicionar novos elementos ao
sistema, o que abre espaço para o livre arbítrio. Existe também espaço para a
infinidade de processos de 'feedback' que fazem com que as experiências sejam
tão profundamente complexas e que são descritas de maneira tão intrigante na
teoria do caos. A realidade não se assemelha a uma simples linha ou círculo.
Ela se assemelha mais às trajetórias bizarras criadas por uma estranha força de
atração ou um conjunto de Mandelbrot.[<a href="#N1">1</a>] <o:p></o:p></span></p>

<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>Como existem
muitas similaridades entre a teoria do caos e as explicações Budistas acerca da
causalidade, parece legítimo explorar essas similaridades, para ver como a
teoria do caos pode ajudar a esclarecer como um caminho de prática causal pode
conduzir a um objetivo que é desprovido de causa. Isso não equivale a igualar o
Budismo à teoria do caos ou de engajar-se em uma pseudo ciência. É simplesmente
uma busca de semelhanças para esclarecer um aparente conflito nos ensinamentos
do Buda. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>E assim sucede que uma das
descobertas da matemática não linear - que é a base da teoria do caos -
esclarece justamente esse assunto. No século 19, o matemático francês
Jules-Henri Poincaré descobriu que em todos os sistemas físicos complexos
existem pontos que ele chamou de ressonâncias. Se as forças que governam um
sistema são descritas por equações matemáticas, as ressonâncias são os pontos
em que as equações se cruzam de tal forma que um dos membros é dividido por
zero. Isto, é claro, produz um resultado indefinido, o que significa que se um
objeto dentro do sistema se desgarrasse para um ponto de ressonância, ele não
mais seria definido pela estrutura causal que determina o sistema. Ele estaria
livre. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Na prática é muito raro que
um objeto encontre um ponto de ressonância. As equações que descrevem os pontos
que se encontram imediatamente ao redor de uma ressonância tendem a desviar
qualquer objeto que se aproxima, de entrar na ressonância, a menos que o objeto
se encontre em uma trajetória precisa em direção ao núcleo da ressonância. No
entanto, não é necessária muita complexidade para criar ressonâncias - Poincaré
as descobriu enquanto calculava as interações gravitacionais entre três corpos:
a terra, o sol e a lua - e quanto mais complexo for o sistema, maior o número
de ressonâncias e maior a probabilidade de que objetos irão se desgarrar na sua
direção. Não é surpresa, que meteoros em uma escala maior e eléctrons em uma
escala menor, ocasionalmente se percam em uma ressonância num campo
gravitacional ou elétrico e assim alcancem a liberdade da completa
imprevisibilidade. Essa é a razão porque o seu computador ocasionalmente trava
sem ter uma razão aparente e porque a mesma coisa pode um dia acontecer com as
batidas do seu coração. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Se formos aplicar esta
analogia ao caminho Budista, o sistema no qual estamos é samsara, o ciclo de
renascimentos. As suas ressonâncias seriam aquilo que os textos chamam de
&quot;não fabricado&quot;, a abertura em direção ao nibbana que não é sujeito a
causas. A parede de forças opostas ao redor das ressonâncias corresponderiam ao
sofrimento, estresse e o apego. Permitir que você seja repelido pelo sofrimento
ou desviado pelo apego, não importando quão sutis sejam, seria o mesmo que
aproximar-se de uma ressonância para então ser desviado para uma outra parte do
sistema. Mas focar diretamente na análise do sofrimento e do apego e desmontar
as suas causas, seria como estar em uma trajetória direta para a ressonância
para encontrar a completa, indefinível liberdade. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isto, é claro, é uma
simples analogia. Mas é uma analogia proveitosa para mostrar que não existe
nada de ilógico em ativamente perseguir o controle sobre os processos de
fabricações mentais e da causalidade com o objetivo de ir além da causa e
efeito. Ao mesmo tempo, dá uma dica de porque um caminho de total inércia não
conduziria ao não fabricado. Se você simplesmente sentar-se quieto dentro do
sistema de causalidade, você nunca irá chegar próximo das ressonâncias onde o
não fabricado está. Você ficará flutuando no samsara. Mas se você toma como
objetivo o sofrimento e o apego e trabalha para desmantelá-los, você será capaz
de romper a barreira depois da qual o momento presente será dividido por zero
na mente. <o:p></o:p></span></p>


