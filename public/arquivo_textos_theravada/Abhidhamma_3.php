<p class=Tit1 align=center style='text-align:center'>III. Compêndio da Matéria <i>(rupa)</i></Tit1>


<p class=Normal>&nbsp;</p>

<hr size=2 width="100%" align=center>

<p class=Normal>&nbsp;</p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A palavra
em Pali para matéria, <i>rupa</i>, é explicada como sendo uma derivação do
verbo <i>ruppati</i>, que significa “ser deformado, perturbado, golpeado,
oprimido, partido.” Os comentários afirmam que a matéria é assim denominada
porque ela está sujeita a alterações devido a condições físicas adversas tais
como frio e calor, fome e sede, moscas e mosquitos, vento e sol, etc. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O Compêndio
da Matéria está dividido em cinco partes: 1. Enumeração, 2. Classificação, 3.
Origem, 4. Agrupamento e Modos de Ocorrência (sendo que este último não será
abordado neste trabalho).<o:p></o:p></span></p>

<p class=Normal><a name=T1></a><b>Enumeração dos Fenômenos
Materiais</b><i>(rupasamuddesa)</i><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O
Abhidhamma enumera 28 tipos de fenômenos materiais que estão agrupados em duas
categorias: os quatro grandes elementos essenciais e os fenômenos materiais
derivados deles. Os <i>quatro grandes elementos</i> são os elementos materiais
primários: água, terra, fogo e ar. Esses são os constituintes fundamentais
inseparáveis da matéria e que em suas várias combinações fazem parte da
composição de todas as substâncias materiais, da menor partícula até a maior
montanha. Os <i>fenômenos materiais derivados</i> são como o próprio nome diz
derivados ou dependentes dos quatro grandes elementos. No total, os fenômenos
materiais derivados são 24 em número. Os grandes elementos podem ser comparados
com a terra e os fenômenos derivados com os arbustos e árvores que crescem na
dependência da terra. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O conjunto
dos 28 tipos de fenômenos materiais está dividido em dois grandes grupos: um
grupo é o da matéria produzida de forma concreta, que possui natureza
intrínseca e assim é passível de contemplação através do insight. E o outro,
sendo mais abstrato em sua natureza, é denominado como matéria produzida de
forma não-concreta. Abaixo encontram-se relacionados os dois grupos dos
fenômenos materiais:<o:p></o:p></span></p>
