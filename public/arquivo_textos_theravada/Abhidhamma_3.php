<p class=Normal><b>Matéria Produzida de forma concreta </b><i>(nipphannarupa)<o:p></o:p></i></span></h4>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Estes
dezoito tipos de fenômenos materiais enumerados acima podem ser agrupados como:<o:p></o:p></span></p>

<p class=Normal><i><span lang=PT-BR style='mso-ansi-language:PT-BR'>matéria
que possui natureza intrínseca</span></i><span lang=PT-BR style='mso-ansi-language:
PT-BR'> porque cada tipo possui uma<span style="mso-spacerun: yes">  </span>natureza
objetiva distinta, tal como a dureza no caso do elemento terra, etc.;<o:p></o:p></span></p>

<p class=Normal><i><span lang=PT-BR style='mso-ansi-language:PT-BR'>matéria
que possui características reais </span></i><span lang=PT-BR style='mso-ansi-language:
PT-BR'>porque eles possuem as três marcas: impermanência, sofrimento e
não-eu;<span style="mso-spacerun: yes"></p>
<p class=Normal><i>matéria produzida de forma
concreta </i>porque eles são diretamente produzidos por condições como kamma,
etc.; <o:p></o:p></span></p>

<p class=Normal><i><span lang=PT-BR style='mso-ansi-language:PT-BR'>matéria
material</span></i><span lang=PT-BR style='mso-ansi-language:PT-BR'> porque
eles possuem a característica essencial da matéria de estar sujeita à
deformação;<span style="mso-spacerun: yes">  </span><o:p></o:p></span></p>

<p class=Normal><i><span lang=PT-BR style='mso-ansi-language:PT-BR'>matéria
para ser compreendida através do insight</span></i><span lang=PT-BR
style='mso-ansi-language:PT-BR'> porque eles devem ser empregados como objetos
do insight através da contemplação das três marcas.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(I) O <b>elemento
terra</b> é assim chamado porque, como a terra, ele serve como suporte ou
fundação para os fenômenos materiais coexistentes. O elemento terra tem a
característica da dureza.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O <b>elemento
água</b> ou fluidez, é o fator material que faz com que as diferentes
partículas da matéria fiquem coesas, evitando que elas se espalhem.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O <b>elemento
fogo</b> tem a característica do calor, a sua função é amadurecer os outros
fenômenos materiais. Tanto o calor como o frio são os modos como o elemento
fogo é sentido.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O <b>elemento
ar</b> é o princípio do movimento e da pressão. A sua função é causar o
movimento dos outros fenômenos materiais. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
conjunto, os quatro elementos estão suportados pelo elemento terra, unidos pelo
elemento água,<span style="mso-spacerun: yes">  </span>mantidos pelo elemento
fogo e distendidos pelo elemento ar.<o:p></o:p></span></p>


<p class=Normal><a name=sensitivo></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>(II) <b>Fenômenos sensitivos</b> são os cinco tipos de matéria
localizada em cada um dos cinco órgãos dos sentidos. A sensibilidade deve ser
distinguida do órgão em si, que é o que lhe dá suporte. Aquilo que
convencionamos chamar de olho, no Abhidhamma, é chamado de o composto do olho,
um composto de vários fenômenos materiais. Entre eles está a sensibilidade no
olho, a substância sensível na retina que registra a luz e a cor e que serve
como base física e porta para a consciência no olho. Uma descrição semelhante
se aplica ao ouvido, nariz, língua e corpo, exceto que no corpo a sensibilidade
se estende a todo o corpo e serve para registrar as sensações tangíveis.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(III) <b>Fenômenos
objetivos</b> são os cinco campos sensoriais, (objetos tangíveis), que servem
como suporte objetivo para a respectiva consciência nos órgãos dos sentidos.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(IV) <b>Fenômenos
sexuais</b> são as duas faculdades que possuem as características do sexo
feminino ou do sexo masculino. A sua manifestação são as marcas, sinais e
características femininas e masculinas.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(V) <b>Base
do coração</b>:<span style="mso-spacerun: yes">  </span>Uma base é o suporte
físico para a ocorrência da consciência. A base do coração serve como o suporte
físico para todos os tipos de consciência, exceto aqueles originados nos cinco
meios dos sentidos, que tomam os fenômenos sensitivos como sua base.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(VI) <b>Faculdade
vital</b>: é a contrapartida material da faculdade vital mental. Vida ou
vitalidade é chamada de faculdade porque possui uma influência dominante sobre
os seus adjuntos. A faculdade vital tem a característica de manter os tipos de
matéria coexistindo no momento da sua presença. A sua função é fazer com que
eles ocorram.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(VII) <b>Alimento</b>
tem como característica ser uma essência nutritiva, (ou em outras palavras,
todo alimento pode gerar energia). Sua função é dar sustento ao corpo físico.<o:p></o:p></span></p>


<p class=Normal><b>Matéria não concreta</b> <i>(anipphannarupa)</i></span></h4>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os tipos de
matéria contidos neste grupo são designados como não concretos porque eles não
surgem das quatro causas para a origem da matéria, (veja abaixo – A Origem da
Matéria), mas existem como modalidades ou atributos da matéria concreta. O
Abhidhammattha Sangaha traz mais detalhes deste tipo de matéria mas isto não
será objeto deste trabalho.<o:p></o:p></span></p>


<p class=Normal><a name=T2></a><b>2. Classificação da Matéria (<i>rupavibhaga</i>)</b><o:p></o:p></span></h2>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A matéria
pode ser <b>única</b>: <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Toda
matéria é desprovida de raiz, (delusão, aversão, cobiça), porque não está
associada com as raízes saudáveis, insalubres ou indeterminadas, a associação
com as raízes está limitada aos fenômenos mentais. Toda matéria possui
condições porque ela surge na dependência de quatro causas, (veja abaixo– A
Origem da Matéria). A matéria está sujeita às contaminações, porque pode se
tornar um objeto das quatro impurezas,(<i>asavas</i>). A matéria é condicionada
e mundana porque não há matéria que transcenda o mundo dos cinco agregados.
Toda matéria é da esfera sensual: embora a matéria exista no plano da matéria
sutil, ela pertence, por sua natureza, à esfera sensual porque é o objeto do
desejo sensual. A matéria não possui um objeto, pois ao contrário dos fenômenos
mentais, a matéria não é capaz de perceber um objeto. A matéria não pode ser
abandonada, tal como as contaminações que são abandonadas através dos quatro
caminhos supramundanos.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A matéria
pode ser <b>múltipla</b>: <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os cinco
tipos de fenômenos sensitivos são considerados como internos, todos os demais
são externos, (esta é uma definição técnica do Abhidhamma, embora outros
fenômenos materiais ocorram dentro do corpo, apenas os cinco fenômenos
sensitivos são classificados como internos). Os cinco fenômenos sensitivos mais
a base do coração são considerados bases dos sentidos. Os cinco fenômenos
sensitivos mais os dois meios de manifestação são considerados como portas dos meios
dos sentidos. Uma base é o suporte físico para a ocorrência da consciência. Uma
porta é o canal através do qual as <i>cittas</i> e os <i>cetasikas</i> de um
processo cognitivo ganham acesso ao objeto. Embora as cinco primeiras bases
coincidam com as cinco primeiras portas, isto é, a sensibilidade nos órgãos dos
sentidos, uma base não é idêntica à porta já que ambas desempenham papéis
diferentes na origem da consciência. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os quatro
grandes elementos mais os quatro derivativos – cor, aroma, sabor e essência
nutritiva - são conhecidos como <i>matéria ou fenômenos materiais inseparáveis</i>
porque eles estão sempre em conjunto e estão presentes em todos os objetos
materiais, dos mais elementares aos mais complexos. Os outros tipos de
fenômenos materiais podem estar presentes ou não e por isso são considerados
como separáveis.<o:p></o:p></span></p>

<p class=Normal><a name=T3></a><b>3. A Origem da Matéria</b><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os
fenômenos materiais podem se originar de quatro formas: de <i>kamma</i>, da
consciência, da temperatura e do alimento.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>1. <i>Kamma</i>:
se refere à volição, (<i>cetana</i>), presente em estados de consciência
saudáveis ou insalubres. Existem vinte cinco tipos de <i>kamma</i> que produzem
fenômenos materiais: as doze <i>cittas</i> insalubres da esfera da
sensualidade, as oito <i>cittas</i> saudáveis da esfera da sensualidade e as
cinco <i>cittas</i> saudáveis da esfera da materialidade sutil. As <i>cittas</i>
saudáveis da esfera imaterial geram o renascimento no plano imaterial e dessa
forma não produzem fenômenos materiais originados de kamma. Dezoito tipos de
fenômenos materiais são produzidos por <i>kamma</i>: os oito fenômenos
materiais inseparáveis nos nove grupos produzidos por kamma, (veja abaixo –
Agrupamento de Fenômenos Materiais); os cinco tipos de fenômenos sensitivos; as
duas faculdades sexuais; a faculdade vital da matéria; a base do coração; e o
espaço.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>2.
Consciência: existem vários fenômenos materiais produzidos pelos distintos
estados de consciência: os fenômenos materiais que ocorrem a partir do
surgimento do primeiro momento de consciência, após a consciência de
renascimento; na manutenção das posturas corporais; na manifestação verbal e
corporal; no sorriso ou no riso.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>3. Temperatura:
começando com a consciência de renascimento, o elemento fogo interno presente
nos grupos materiais formados a partir do <i>kamma</i> se combina com o
elemento fogo externo e começa a produzir fenômenos materiais orgânicos
originados do calor. Depois disso, o elemento fogo, presente em todos os
fenômenos materiais, produz fenômenos materiais orgânicos nascidos do calor ao
longo de toda a existência. Externamente, a temperatura ou o elemento fogo
também produz fenômenos materiais inorgânicos tais como as transformações
climáticas e geológicas.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>4.
Alimento: o alimento produz fenômenos materiais a partir do momento em que é
engolido.<o:p></o:p></span></p>


<p class=Normal><a name=T4></a><b>4. O Agrupamento de Fenômenos
Materiais</b><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os
fenômenos materiais não ocorrem de forma isolada mas em combinações ou grupos conhecidos
como <a name=PVW><i>rupakalapas</i></a>, sendo que existem 21 grupos
enumerados, (abaixo encontram-se relacionados apenas 14 grupos a título de
exemplo). Todos os fenômenos materiais num grupo surgem juntos e cessam juntos.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Grupos
que se originam do kamma</span></b><span lang=PT-BR style='mso-ansi-language:
PT-BR'>: a faculdade vital, junto com os oito fenômenos materiais inseparáveis,
juntamente com o olho formam o grupo do olho. Da mesma forma com o ouvido,
nariz, língua, corpo, feminilidade, masculinidade e base do coração. Os oito
fenômenos materiais inseparáveis mais a faculdade vital é o grupo vital. Esses
nove grupos se originam de kamma.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Grupos
que se originam da consciência</span></b><span lang=PT-BR style='mso-ansi-language:
PT-BR'>: Os oito fenômenos materiais inseparáveis junto com a manifestação
corporal formam o grupo da manifestação corporal; junto com a manifestação
verbal mais o som formam o grupo da manifestação verbal.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Grupos
que se originam da temperatura</span></b><span lang=PT-BR style='mso-ansi-language:
PT-BR'>: O grupo dos oito inseparáveis; o grupo do som.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Grupos
que se originam do alimento</span></b><span lang=PT-BR style='mso-ansi-language:
PT-BR'>: O grupo dos oito inseparáveis.<o:p></o:p></span></p>


