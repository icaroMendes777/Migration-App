<p class=Normal><b>Breve Histórico da Tradição das Florestas da Tailândia</b></p>



<p class=Normal> Phra Ajaan Sao Kantasilo (1861-1941) e Phra Ajaan Mun Bhuridatto (1870-1949) são considerados como os originadores da tradição de monges <a href="../glossario.php#dhutanga">dhutanga</a>, os meditadores das florestas na região nordeste da Tailândia. Essa crença tem como base a premissa de que as tradições das florestas anteriores haviam desaparecido e que Ajaan Sao e Ajaan Mun foram os responsáveis pela sua restauração. Historicamente essa interpretação não é acurada visto que a prática dhutanga era na época seguida por muitas das tradições regionais do antigo Sião, (moderna Tailândia). </p>

<p class=Normal> No antigo Sião existia uma grande variedade de tradições regionais nas quais as regras do Vinaya eram flexibilizadas de acordo com os costumes locais, e  nessas tradições havia monges que se dedicavam à prática da meditação. Em 1828, o príncipe Tailandês Mongkut (que se tornaria o rei Rama IV),  estabeleceu a tradição Dhammayut que defendia o estrito rigor na adoção das regras do Vinaya e uma ênfase nos estudos dos ensinamentos dos Suttas, Abhidhamma e Comentários, porém com uma abordagem mais crítica e racionalista. Nessa tradição, a prática da meditação era considerada inferior ao estudo dos ensinamentos. Com o surgimento da tradição Dhammayut, todas as demais tradições regionais foram categorizadas como um grupo, que foi denominado Mahanikaya.  </p>

<p class=Normal> Antes da centralização da Sangha, através da lei da Sangha promulgada pelo Rei Rama V em 1902, havia abades de monastérios Dhammayut em Ubon, região nordeste do Sião, que eram meditadores e que seguiam estritamente as regras do Vinaya. Eles também proibiam os monges de participar de festas populares e de projetos de construção, práticas  muito comuns nas demais tradições regionais. Ajaan Sao havia sido ordenado na tradição Lao e era o abade de um monastério em Ubon. Ele ficou impressionado com a disciplina desses monges Dhammayut e juntamente como o seu discípulo Ajaan Mun, decidiram se converter para a tradição Dhammayut. Uma vez ordenados, eles ficaram livres das tarefas típicas dos monges das tradições regionais e passaram  a se concentrar completamente na prática dhutanga de meditação. Assim foi como, no final do século 19, Ajaan Sao deu origem à tradição das florestas dhutanga na tradição Dhammayut. </p>

<p class=Normal> A principal diferença entre a prática dhutanga Dhammayut e a das demais tradições regionais dizia respeito às regras do Vinaya. Enquanto os monges Dhammayut obedeciam as 227 regras prescritas pelo Patimokkha, os monges das tradições regionais seguiam regras de disciplina distintas que, por exemplo, permitiam o armazenamento de alimentos nas cavernas que eles habitavam, a colheita de frutas e vegetais e o preparo de medicamentos à base de ervas. Como resultado, muitos dos monges das tradições regionais eram vistos pelas autoridades centrais da Sangha, em Bangcoc, como relapsos, e considerados como monges Budistas não autênticos. Muitos dos discípulos de Ajaan Mun iniciaram as suas carreiras monásticas nas tradições regionais e só mais tarde foram re-ordenados na tradição Dhammayut, sendo que outros nunca foram re-ordenados na tradição Dhammayut, permanecendo nas suas tradições regionais. </p>

<p class=Normal> Ao longo da primeira metade do século 20, as autoridades da Sangha, em Bangcoc, obedecendo os ditames da lei da Sangha promulgada em 1902, consideravam a prática de meditação dos monges dhutanga como inferior aos estudos do Cânone em Pali. E isso acabou gerando muitos conflitos e situações difíceis para os monges dhutanga. 
Foi só a partir da década de 70 que os monges dhutanga Dhammayut acabaram obtendo mais reconhecimento devido à atenção que receberam da família real, do governo e da mídia na Tailândia. Também nessa época, devido ao intenso desmatamento na região nordeste da Tailândia, os monges dhutanga acabaram se estabelecendo em monastérios, dessa forma dando fim à prática da perambulação pelas florestas. </p>

<p class=Normal> A tradição das florestas imigrou para o Ocidente principalmente através de discípulos Ocidentais da linhagem de Ajaan Chah  que instalaram vários monastérios na Inglaterra, Estados Unidos, Canadá, Suiça, Itália, Austrália e Nova Zelândia. </p> 

<p class=Normal 'style=margin-left:1.0in'> Fonte: <i>Forest Recollections – Wandering Monks in Twentieth-Century Thailand</i>,<br> por Kamala Tiyavanich. </p>


