<p class=Normal><a name=Mun></a><b> Phra  Ajaan Phra Ajaan Mun Bhuridatto  (1870-1949)</span></b> <a
href="#Top"><span style='text-decoration:none;text-underline:none'><img border=0 title="Topo" src="../img/tup.gif"></span></a>
<a name=Top></a><o:p></o:p></span></p>

<p class=Normal>Phra Ajaan Mun Bhuridatto Thera nasceu em 1870 em Baan Kham Bong, um vilarejo agrícola na província de Ubon Ratchathani, no nordeste da Tailândia. Ordenado como monge Budista em 1893, ele passou o resto de sua vida perambulando pela Tailândia, Birmânia e Laos, vivendo na maior parte do tempo na floresta, dedicado à prática de meditação. Ele atraiu um grande número de discípulos e, juntamente com seu mestre, Phra Ajaan Sao Kantasilo, foi responsável pelo estabelecimento da tradição asceta das florestas que agora se espalhou pela Tailândia e muitos outros países. Seu passamento ocorreu em 1949, no Wat Suddhavasa, província de Sakon Nakhorn.</p> <p class=Normal>Muito foi escrito sobre a sua vida, mas muito pouco se registrou dos seus ensinamentos durante o período em que ele esteve vivo. Ele deixou a maior parte dos seus ensinamentos na forma de pessoas: os discípulos cujas vidas foram profundamente influenciadas pela experiência de viver e praticar meditação sob sua orientação.</p>
