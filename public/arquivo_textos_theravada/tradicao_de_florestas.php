<p class=Normal><a name=Chah></a><b>Phra Ajaan Chah  (1918-1992)</span></b> <a
href="#Top"><span style='text-decoration:none;text-underline:none'><img border=0 title="Topo" src="../img/tup.gif"></span></a>
<a name=Top></a>
<o:p></o:p></span></b></p>

<p class=Normal> Ajaan Chah nasceu em 1918 num vilarejo na zona rural da província de Ubon Rachathani, na região nordeste da Tailândia. Ele tinha 5 irmãos e 4 irmãs e de acordo com os padrões da época, a sua família era próspera. Seguindo os costumes da época, aos 13 anos de idade ele se ordenou por 3 anos como noviço no monastério do seu vilarejo, durante esse período ele aprendeu a ler e a escrever, além dos ensinamentos básicos sobre o Budismo. Depois desse período ele regressou à vida leiga para ajudar a sua família. Ele, no entanto, preferia a vida monástica e com 20 anos de idade foi ordenado como bhikkhu. Nos seus primeiros anos de vida como bhikkhu, ele aprendeu a recitar o Patimokkha e estudou os textos em Pali, tendo sido aprovado no nível mais alto dos exames de um bhikkhu. Mas o falecimento do seu pai fez como que ele despertasse para a transitoriedade da vida e fez com que ele refletisse de modo profundo sobre o propósito da vida, pois embora ele tivesse estudado muito e conhecesse o Pali, ele não sentia estar mais próximo de compreender o fim do sofrimento. Desencantado com os estudos e tendo o desejo de buscar a verdadeira essência dos ensinamentos do Buda, em 1946 ele adotou a vida de um bhikkhu perambulante. Ele caminhou cerca de 400 km na direção da região central da Tailândia dormindo nas florestas e esmolando comida nos vilarejos. Num monastério ele ouviu falar sobre o Venerável Ajaan Mun e depois de buscá-lo durante algum tempo ele acabou passando um periodo curto porém fértil com Ajaan Mun. Ajaan Mun exerceu uma influência indelével sobre Ajaan Chah, proporcionando direção e claridade que faltavam à sua meditação. Outros dois mestres mencionados por Ajaan Chah de modo reverencioso eram Ajaan Kinnaree e Ajaan Tongrat.<o:p></o:p></span></p>

<p class=Normal> Durante os 7 anos seguintes Ajaan Chah se dedicou ao estilo austero da tradição das florestas da Tailândia, vivendo nas florestas infestadas de animais selvagens, em cavernas e cemitérios, locais ideais para a prática da meditação. Depois desses muitos anos perambulando, em 1954, ele foi convidado, junto com um punhado de discípulos, a se estabelecer numa densa floresta próximo ao vilarejo onde ele havia nascido. O seu estilo simples e direto, com ênfase na aplicação prática dos ensinamentos e uma atitude balanceada, começaram a atrair cada vez mais um grande número de bhikkhus e leigos acabando por formar um grande monastério, Wat Nong Pah Pong, no local onde Ajaan Chah havia se estabelecido. A essência do seu ensinamento era particularmente simples: esteja plenamente atento, não se apegue a nada, solte-se de tudo e submeta-se às coisas da maneira como elas são.<o:p></o:p></span></p>

<p class=Normal> O estilo de ensino simples, no entanto profundo, de Ajaan Chah tinha um apelo especial para os 
Ocidentais.  
Em 1966, o
primeiro Ocidental veio para ficar no Wat Pah Pong, o Venerável Sumedho Bhikkhu.
Daquele momento em diante, o número de estrangeiros que buscaram Ajaan Chah foi
crescendo continuamente, até que em 1975, foi estabelecido Wat Pah Nanachat, um monastério especial para treinar o 
crescente número de Ocidentais, tendo o Venerável Ajaan Sumedho como abade.<o:p></o:p></span></p> 

<p class=Normal>Em 1979, o primeiro de muitos monastérios na Europa, Chithurst Buddhist Monastery, 
foi estabelecido em Sussex, na Inglaterra, pelos seus discípulos Ocidentais mais graduados (entre eles Ajaan Sumedho). 
Na atualidade, fora da Tailândia, existem 15 monastérios com mais de 60 monges espalhados pelos Estados Unidos, Canadá, 
Europa, Austrália e Nova Zelândia.  Veja <img src="img/globe.gif" border=0> <a href="http://www.forestsangha.org">Forest Sangha</a>.

<p class=Normal> Em 1980 Ajaan Chah começou a sentir de maneira mais aguda os sintomas de
vertigem e lapsos de memória que ele vinha sentindo ao longo de alguns anos.
Isso acarretou uma cirurgia em 1981 que, no entanto, fracassou no seu objetivo
de reverter o processo de paralisia, que, afinal, fez com que ele ficasse confinado
à cama e impossibilitado de falar. No entanto, isso não inibiu o crescimento do
número de monges e pessoas leigas que vinham para praticar no seu monastério,
para eles os ensinamentos de Ajaan Chah
serviam como guia e inspiração permanentes. Ajaan Chah faleceu em Janeiro de 1992.
<o:p></o:p></span></p>

