<p class=Tit1 align=center style='text-align:center'>Bhikkhu Khantipalo</Tit1>

<p class=Normal>Bhikkhu Khantipalo nasceu em 1932 como Laurence Mills. Ele foi ordenado como 
bhikkhu na Índia no final da década de 1950. Ele estudou meditação na Índia e na Tailândia 
até 1973 quando se mudou para a Austália em 1978 e juntamente com Ayya Khema estabeleceram o 
Wat Buddha Dhamma, onde ele serviu durante muitos anos como  professor residente. 
Mais tarde ele retornou para a vida leiga e se converteu ao Budismo Tibetano para dar seguimento 
ao seu interesse na meditação Dzogchen, uma prática de certa forma similar à meditação Vipassana. 
Atualmente ele é o responsável por um Centro de Meditação na região de North Queensland na Austrália.<o:p></o:p></span></p>
