<p class=Normal style='margin-left:.75in'> “Bhikkhus, há essas três causas para a origem de kamma. Quais três? A cobiça é uma causa para a origem de kamma, a raiva é uma causa para a origem de kamma, e a delusão é uma causa para a origem de kamma.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>(1) "Qualquer kamma criado através da cobiça, nascido da cobiça, causado pela cobiça, originado da cobiça, esse kamma é prejudicial e repreensível e resulta no sofrimento. Esse kamma conduz ao surgimento de kamma, não à cessação de kamma.</p>

<p class=Normal style='margin-left:.75in'>(2) "Qualquer kamma criado através da raiva ... (3) Qualquer kamma criado criado através da delusão, nascido da delusão, causado pela delusão, originado da delusão, esse kamma é prejudicial e repreensível e resulta no sofrimento. Esse kamma conduz ao surgimento de kamma, não à cessação de kamma.</p>

<p class=Normal style='margin-left:.75in'>“Bhikkhus, há essas outras três causas para a origem de kamma. Quais três? A não-cobiça é uma causa para a origem de kamma, a não-raiva é uma causa para a origem de kamma, e a não-delusão é uma causa para a origem de kamma.</p>

<p class=Normal style='margin-left:.75in'> (1) "Qualquer kamma criado criado através da não-cobiça, nascido da não-cobiça, causado pela não-cobiça, originado da não-cobiça, esse kamma é benéfico e irrepreensível e resulta na felicidade. Esse kamma conduz à cessação de kamma, não ao surgimento de kamma. </p>

<p class=Normal style='margin-left:.75in'> (2) "Qualquer kamma criado através da não-raiva ... (3) Qualquer kamma criado através da não-delusão, nascido da não-delusão, causado pela não-delusão, originado da não-delusão, esse kamma é benéfico e irrepreensível e resulta na felicidade. Esse kamma conduz à cessação de kamma, não ao surgimento de kamma. [AN.I.263] (<a href="../sutta/ANIII.111.php">AN III.111</a>)<o:p></o:p></span></p>

<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-language:PT-BR'>* * *<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> “Bhikkhus, matar seres vivos, eu digo, é de três tipos: causado pela cobiça, causado pela raiva, causado pela delusão. Tomar aquilo que não foi dado, eu digo, também é de três tipos: causado pela cobiça, causado pela raiva, causado pela delusão. A conduta sexual imprópria, eu digo, é de três tipos: causado pela cobiça, causado pela raiva, causado pela delusão. A linguagem mentirosa, eu digo, é de três tipos: causado pela cobiça, causado pela raiva, causado pela delusão. A linguagem maliciosa, eu digo, é de três tipos: causado pela cobiça, causado pela raiva, causado pela delusão. A linguagem grosseira, eu digo, é de três tipos: causado pela cobiça, causado pela raiva, causado pela delusão. A linguagem frívola eu digo, é de três tipos: causado pela cobiça, causado pela raiva, causado pela delusão. A cobiça, eu digo, é de três tipos: causado pela cobiça, causado pela raiva, causado pela delusão. A má vontade, eu digo, é de três tipos: causado pela cobiça, causado pela raiva, causado pela delusão. O entendimento incorreto, eu digo, é de três tipos: causado pela cobiça, causado pela raiva, causado pela delusão.</p>

<p class=Normal style='margin-left:.75in'> “Portanto, bhikkhus, a cobiça é a fonte e origem de kamma; a raiva é a fonte e origem de kamma; a delusão é a fonte e origem de kamma. Com a destruição da cobiça, a fonte de kamma é extinta. Com a destruição da raiva, a fonte de kamma é extinta. Com a destruição da delusão, a fonte de kamma é extinta.” [AN.V.261] (<a href="../sutta/ANX.174.php">AN X.174</a>)<o:p></o:p></span></p>

<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-language:PT-BR'>* * *<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> “Bhikkhus, esses quatro tipos de kamma foram diretamente compreendidos, verificados e anunciados por mim. Quais quatro? Há o kamma que é escuro com um resultado sombrio. Há o kamma que é claro com um resultado luminoso. Há o kamma que é escuro e claro com um resultado sombrio e luminoso. Há o kamma que é nem escuro nem claro com um resultado nem sombrio nem luminoso, que conduz ao fim de kamma.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> “E qual é o kamma que é escuro com um resultado sombrio? É o caso em que uma certa pessoa fabrica uma formação corporal prejudicial, fabrica uma formação verbal prejudicial, fabrica uma formação mental prejudicial. Tendo fabricado uma formação corporal prejudicial, tendo fabricado uma formação verbal prejudicial, tendo fabricado uma formação mental prejudicial, ela renasce num mundo com aflição. Ao renascer num mundo com aflição, ela é tocada por contatos aflitivos. Tocada por contatos aflitivos, ela experimenta sensações que são exclusivamente dolorosas, como aquelas experimentadas pelos seres no inferno. Isso é chamado kamma que é escuro com um resultado sombrio.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> “E qual é o kamma que é claro com um resultado luminoso? É o caso em que uma certa pessoa fabrica uma formação corporal não prejudicial, fabrica uma formação verbal não prejudicial, fabrica uma formação mental não prejudicial .... ela renasce num mundo sem aflição ... ela é tocada por contatos não aflitivos ...ela experimenta sensações que são exclusivamente prazerosas, como aquelas experimentadas pelos devas luminosos. Isso é chamado kamma que é claro com um resultado luminoso.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>“E qual é o kamma que é escuro e claro com um resultado sombrio e luminoso? É o caso em que uma certa pessoa fabrica uma formação corporal prejudicial e fabrica uma formação corporal não prejudicial, fabrica uma formação verbal prejudicial e fabrica uma formação verbal não prejudicial, fabrica uma formação mental prejudicial e fabrica uma formação mental não prejudicial .... ela renasce num mundo com aflição e sem aflição ... ela é tocada por contatos aflitivos e não aflitivos ...ela experimenta sensações que são dolorosas e prazerosas, como aquelas experimentadas pelos seres humanos, alguns devas e alguns seres nos mundos inferiores. Isso é chamado kamma que é escuro e claro com um resultado sombrio e luminoso.</p>

<p class=Normal style='margin-left:.75in'> E qual é o kamma que é nem escuro nem claro com um resultado nem sombrio nem luminoso, que conduz ao fim de kamma? Entendimento correto, pensamento correto, linguagem correta, ação correta, modo de vida correto, esforço correto, atenção plena correta, concentração correta. Isso é chamado kamma que é nem escuro nem claro com um resultado nem sombrio nem luminoso, que conduz ao fim de kamma.” [AN.II.233] (<a href="../sutta/ANIV.235.php">AN IV.235</a>)<o:p></o:p></span></p>

<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-language:PT-BR'>* * *<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>“Udayi, um bhikkhu desenvolve 
o fator da iluminação da atenção plena, que tem como base o afastamento, desapego e 
cessação, que amadurece no abandono; que é vasto, glorificado, imensurável, sem má vontade. 
Ao desenvolver o fator da iluminação da atenção plena, que tem como base o afastamento ... sem má vontade, 
o desejo é abandonado. Com o abandono do desejo, kamma é abandonado. Com o abandono de kamma, 
o sofrimento é abandonado ... Ele desenvolve o fator da iluminação da equanimidade, que tem como base o 
afastamento, desapego e cessação, que amadurece no abandono; que é vasto, glorificado, imensurável, 
sem má vontade. Ao desenvolver o fator da iluminação da atenção plena, que tem como base o afastamento ... sem má vontade, 
o desejo é abandonado. Com o abandono do desejo, kamma é abandonado. Com o abandono do kamma, o sofrimento é abandonado.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>“Portanto, Udayi, com a destruição do desejo ocorre 
a destruição do kamma, com a destruição do kamma ocorre a destruição do sofrimento.” [SN.V.86] (<a href="../sutta/SNXLVI.26.php">SN XLVI.26</a>)<o:p></o:p></span></p>



