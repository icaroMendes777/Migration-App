<p class=Tit1 align=center style='text-align:center'>5. O Kamma que dá fim ao Kamma</Tit1>

<p class=Normal>&nbsp;</p>

<hr size=2 width="100%" align=center>

<p class=Normal>&nbsp;</p>

<p class=Normal>Na última parte do Capítulo 1, foram mencionados
quatro tipos diferentes de kamma, classificados de acordo com a sua relação com
os seus respectivos resultados:<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>1. Kamma escuro com um resultado
sombrio.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>2. Kamma claro com um resultado
Luminoso.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>3. Kamma escuro e claro com um resultado sombrio e luminoso.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>4. Kamma nem escuro, nem claro com um resultado nem sombrio, nem luminoso,
que conduz à cessação de kamma.[MN.I.389] (<a href="../sutta/MN57.php">MN 57</a>)<o:p></o:p></span></p>

<p class=Normal> Todas as variedades de resultados de kamma descritas
até o momento estiveram restritas às três primeiras categorias, kamma claro,
kamma escuro, e ambos, claro e escuro ou kamma bom e kamma ruim. O quarto tipo
de kamma falta ser explicado. Como este quarto tipo de kamma apresenta um
resultado totalmente distinto dos outros três, foi dado a ele um capítulo
próprio em separado. <o:p></o:p></span></p>

<p class=Normal> Para a maioria das pessoas, incluindo Budistas, todo
interesse em kamma tende a estar centrado em torno dos primeiros três tipos de
kamma, deixando totalmente de lado o quarto tipo, muito embora este último tipo
de kamma seja um dos ensinamentos essenciais do Budismo e o que conduz ao seu
objetivo último.<o:p></o:p></span></p>

<p class=Normal> Kamma escuro, claro e escuro-claro são em geral
descritos como os vários tipos de ações que fazem parte das dez bases para
ações inábeis, tal como matar seres vivos, violar as propriedades dos outros,
conduta sexual imprópria e linguagem nociva ou maliciosa, com os seus respectivos
opostos que constituem as ações hábeis. Esses tipos de kamma são os
determinantes para vários tipos de experiências de vida, boas e ruins, como foi
explicado antes. Os eventos da vida por seu lado ativam mais kamma bom e ruim,
dessa forma fazendo girar a roda do samsara de forma indefinida.<o:p></o:p></span></p>

<p class=Normal> O quarto tipo de kamma produz resultados de forma
exatamente oposta. Ao invés de causar a acumulação de mais kamma, ele conduz à
cessação de kamma. Na verdade isto se refere à prática que conduz ao objetivo
máximo do Budismo, a Libertação, tal como o Nobre Caminho Óctuplo, também
conhecido como o Treinamento Tríplice (Virtude, Concentração e
Sabedoria) ou os Sete Fatores da Iluminação. Algumas vezes este quarto tipo de
kamma é referido como a intenção, baseada na não-cobiça, não-raiva e
não-delusão, de abandonar os outros três tipos de kamma.<o:p></o:p></span></p>

<p class=Normal> Nenhuma discussão sobre kamma deveria deixar de
mencionar a felicidade e o sofrimento. Kamma é a causa que resulta em
felicidade e sofrimento, e enquanto existir kamma haverá a oscilação entre
esses dois estados. No entanto, ao aspirar ao bem máximo que está isento de
qualquer mácula, qualquer condição marcada pela felicidade ou sofrimento,
estando sujeita à oscilação, será inadequada. Todo kamma mundano ainda está
maculado com o sofrimento e é uma causa do sofrimento.<o:p></o:p></span></p>

<p class=Normal> No entanto, isto apenas é válido para os três
primeiros tipos de kamma. O quarto tipo de kamma está isento, porque conduz à
cessação de kamma e dessa forma à completa cessação do sofrimento. Embora o bom
kamma resulte em felicidade, essa felicidade é maculada pelo sofrimento e pode
ser a causa de sofrimento no futuro. Mas este quarto tipo de kamma, além de ser
em si mesmo livre de sofrimento, também dá origem à libertação
do sofrimento, completa e imaculada. Sendo, portanto, o tipo de felicidade mais
pura.<o:p></o:p></span></p>

<p class=Normal> 
A cessação ou extinção de kamma era ensinada num
grande número de diferentes religiões na época do Buda, de forma especial na
Seita dos Niganthas (Jainistas). Os Niganthas ensinavam o princípio do kamma
passado, a cessação de kamma e a mortificação do corpo de forma a “desgastar” o
kamma passado. Se esses três princípios não forem claramente diferenciados dos
ensinamentos do Buda, poderão ser confundidos com eles com facilidade. E de
modo inverso, distinguindo-os com nitidez dos princípios do Budismo poderá
ajudar a tornar ainda mais clara a mensagem do Buda. Os<span
style="mso-spacerun: yes">  </span>Niganthas ensinavam:<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> Bhikkhus, existem alguns contemplativos e brâmanes que possuem a
seguinte doutrina e entendimento: ‘Qualquer coisa que uma pessoa sinta, quer
seja prazer ou dor, ou nem prazer, nem dor, tudo é causado pelo que foi feito
no passado.<span style="mso-spacerun: yes">  </span><a name=R1></a>Então
aniquilando através do ascetismo as ações passadas <a name=R2></a>e não
cometendo novas ações, não haverá conseqüência no futuro. Sem conseqüência no
futuro, ocorre a destruição da ação. Com a destruição da ação, ocorre a
destruição do sofrimento. Com a destruição do sofrimento, ocorre a destruição
da sensação. Com a destruição da sensação, todo o sofrimento será extinto.’ [MN.II.214] (<a href="../sutta/MN101.php">MN
101</a>)<o:p></o:p></span></p>

<p class=Normal>
Os Niganthas acreditavam que tudo é causado pelo kamma
passado. Para se livrar do sofrimento seria necessário abandonar o kamma
passado e, por meio da prática de austeridades, não acumular kamma novo. Mas o
Budismo afirma que o kamma passado é apenas um dos fatores em todo o processo
de causa e efeito. Este é um ponto importante.<o:p></o:p></span></p>

<p class=Normal>
Kamma pode conduzir à transcendência do sofrimento,
mas tem que ser o tipo certo de kamma, o kamma que evita o surgimento de mais
kamma e assim conduz à sua cessação. Então, de modo a anular kamma, ao invés de
simplesmente ficar quieto e não fazer nada, o praticante Budista tem que se
empenhar numa prática fundamentada no entendimento correto. A prática correta
conduz à independência, clareza e liberdade das diretrizes do desejo que, em
conjunto com a ignorância, enreda os seres na busca por realizações.<o:p></o:p></span></p>

<p class=Normal>
Para esclarecer esse quarto tipo de kamma, as suas
características gerais podem ser resumidas da seguinte forma:<o:p></o:p></span></p>

<p class=Normal>
(a) É o caminho da prática que conduz à cessação de
kamma. Ao mesmo tempo, é em si mesmo um tipo de kamma,<o:p></o:p></span></p>

<p class=Normal>
(b) É conhecido como o “kamma nem escuro, nem claro com um resultado nem
sombrio, nem luminoso, que conduz à cessação de kamma.”<o:p></o:p></span></p>

<p class=Normal>
(c) As suas causas-raiz são a não-cobiça, não-raiva e
não-delusão.<o:p></o:p></span></p>

<p class=Normal>
(d) Está baseado na sabedoria e compreensão das
vantagens e das imperfeições das coisas, como elas na verdade são. É o tipo de
ação impecável, ação que é verdadeiramente benéfica, baseada em fundamentos
íntegros e que conduzem a uma vida saudável.<o:p></o:p></span></p>

<p class=Normal>
(e) Como este tipo de ação não é guiada pelo desejo,
quer seja na forma de aproveitamento egoísta ou na inação baseada no temor de
perda pessoal, é o verdadeiro tipo de esforço altruísta, guiado e suportado
pela atenção plena e sabedoria.<o:p></o:p></span></p>

<p class=Normal>
(f) É <i>kusala</i> kamma, ação hábil, no nível
conhecido como Ação Hábil Transcendente.<o:p></o:p></span></p>

<p class=Normal>
(g) Em relação à prática, pode ser chamado de Caminho
Óctuplo para a cessação do sofrimento, a Quarta das Quatro Nobres Verdades, os
Sete Fatores da Iluminação, ou o Treinamento Tríplice, dependendo do contexto;
faz também referência a um sentido mais geral, como a intenção de abandonar os
primeiros três tipos de kamma.<o:p></o:p></span></p>

<p class=Normal>
Com relação ao ponto (e) acima, vale a pena destacar
que <i>tanha</i>, ou desejo, é visto pela maioria das pessoas como a força que
motiva a ação. No que diz respeito à maioria das pessoas, quanto mais desejo
existir, mais intensa e competitiva será a ação resultante; elas entendem que
sem desejo não há incentivo para agir, e o resultado só poderia ser a inércia e
a preguiça. Esse tipo de entendimento surge da observação incompleta da
natureza humana. Se for empregado como diretriz para a prática, poderá causar
problemas tanto no nível individual como no social.<o:p></o:p></span></p>

<p class=Normal>
Na verdade, o desejo é um ímpeto para ambos, ação e
inação. Na busca de objetos para se alimentar, o desejo é um ímpeto para a
ação. Esse tipo de ação tende a gerar exploração e disputas. No entanto, nas
ocasiões em que ações boas e altruístas são necessárias, o desejo passará a ser
um incentivo para a inação, atando o eu ao conforto pessoal, mesmo que seja
apenas como apego ao sono. Dessa forma, o desejo se torna um obstáculo ou
estorvo na realização de boas ações. Se a ignorância ainda for predominante,
isto é, se não existir compreensão do valor das boas ações, o desejo irá
encorajar a inércia e a negligência. Por essa razão, o desejo pode ser um
incentivo ou para um tipo de atividade exploradora, ou para um tipo de
inatividade letárgica, dependendo do contexto.<o:p></o:p></span></p>

<p class=Normal>
A prática que suporta um estilo de vida saudável e que
é verdadeiramente benéfica é completamente distinta dessa gratificação dos
desejos egoístas e em muitos casos demanda a renúncia dos confortos e prazeres
pessoais. Esse tipo de prática não pode ser concretizada por meio do desejo
(exceto se primeiro qualificarmos nossos termos), mas deve ser realizada
através do entendimento e apreço pelas vantagens dessa prática tal como ela na
verdade é.<o:p></o:p></span></p>

<p class=Normal>
Esse apreço ou aspiração é chamado de <i>chanda</i> em
Pali (conhecido de forma completa como <i>kusalachanda</i> ou <i>dhammachanda</i>).
<i>Chanda</i> é o incentivo real para quaisquer ações verdadeiramente
construtivas. No entanto, este pode ser impedido pelo desejo e o seu apego à
preguiça, letargia ou conforto pessoal. Nesse caso, o desejo irá macular com o
sofrimento todas as tentativas de realizar boas ações, ao resistir à prática
através daqueles estados negativos. Se houver clara compreensão da vantagem
dessas ações e suficiente apreço (<i>chanda) </i>por elas, possibilitando que o
efeito opressor do desejo seja superado, <i>chanda </i>se torna, além de um
ímpeto para a ação, uma causa para a felicidade.<o:p></o:p></span></p>

<p class=Normal>
Esse tipo de felicidade difere da felicidade que
resulta do desejo – ela é leve e despreocupada, ao invés de constritiva e
pesada, e conduz a ações criativas isentas de sofrimento. Neste caso, <i>samadhi,
</i>a mente firmemente estabelecida, composta de esforço, atenção plena e
compreensão, irá se desenvolver e diretamente suportar tais iniciativas. Esse
tipo de prática é conhecida como “o kamma que dá fim ao kamma.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='Ao praticar
de acordo com o Nobre Caminho Óctuplo, o desejo não possui um canal através do qual
possa funcionar e assim ele é eliminado. A cobiça, raiva e delusão não surgem.
Sem desejo, cobiça, raiva ou delusão, não há kamma. Sem kamma, não existem
resultados do kamma para aprisionar a mente. Sem kamma para aprisionar a mente,
emerge um estado de clareza que transcende o sofrimento. A mente, que antes era
escrava do desejo, passa a ser guiada pela sabedoria, dirigindo as ações
independentemente das influências do desejo.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='A seguir
algumas palavras do Buda que tratam do kamma que dá um fim ao kamma.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> &quot;’O kamma deve ser conhecido. A causa porque o kamma se manifesta
deve ser conhecida. A diversidade do kamma deve ser conhecida. O resultado do
kamma deve ser conhecido. A cessação do kamma deve ser conhecida. O caminho da
prática para a cessação do kamma deve ser conhecido. ' Assim foi dito. Em
referência a que foi dito isso?<span style="mso-spacerun: yes">  </span><o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>  &quot;Intenção, eu lhes digo, é kamma. Pela intenção, a pessoa faz kamma
através do corpo, linguagem, e mente. <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> &quot;E qual é a causa porque o kamma se manifesta? Contato é a causa pela
qual kamma se manifesta. <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> &quot;E qual é a diversidade de kamma? Existe o kamma para ser
experimentado no inferno, kamma para ser experimentado no mundo dos animais,
kamma para ser experimentado no mundo dos fantasmas, kamma para ser
experimentado no mundo humano, kamma para ser experimentado no mundo dos devas.
A isto se denomina a diversidade de kamma. <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> &quot;E qual é o resultado do kamma? O resultado do kamma é de três
tipos, eu lhes digo: aquele que surge no aqui e agora, aquele que surge mais
tarde [nesta vida ] e aquele que surge depois desta vida. A isto se denomina o
resultado do kamma. <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> &quot;E qual é a cessação do kamma? Com a cessação do contato, kamma
cessa; e justamente este nobre caminho óctuplo - entendimento correto,
pensamento correto, linguagem correta, ação correta, modo de vida correto,
esforço correto, atenção plena correta, concentração correta - é o caminho da
prática para a cessação do kamma. [AN.III.415] (<a href="../sutta/ANVI.63.php">AN
VI 63</a>)<o:p></o:p></span></p>
<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-language:PT-BR'>* * *<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>"Agora quando um nobre discípulo compreende desse modo kamma, a
causa porque kamma se manifesta, a diversidade de kamma, o resultado de kamma,
a cessação de kamma, e o caminho da prática que conduz à cessação de kamma,
então ele compreende esta penetrante vida santa como sendo a cessação
dkamma.[AN.III.415] (<a href="../sutta/ANVI.63.php">AN VI 63</a>)<o:p></o:p></span></p>

<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-language:PT-BR'>* * *<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> “Bhikkhus, eu lhes explicarei o kamma passado, o kamma novo, a cessação
de kamma e o caminho para a cessação de kamma ... O que é kamma passado?
Olho ... ouvido ... nariz ...língua ... corpo ... mente devem ser compreendidos como
kamma passado, formados por condições, nascidos das volições, a base para as
sensações. Isto é denominado ‘kamma passado.’<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> “Bhikkhus, o que é ‘kamma novo’? Ações criadas através do corpo,
linguagem e mente no momento presente, essas são chamadas ‘kamma novo.’<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> “Bhikkhus, o que é a cessação de kamma? A experiência da libertação, que
surge da cessação de kamma corporal, kamma verbal e kamma mental, é chamada de
cessação de kamma.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> “Bhikkhus, qual é o caminho para a cessação de kamma? É este Nobre
Caminho Óctuplo, isto é, Entendimento Correto....Concentração Correta. Isso é
chamado o caminho que conduz à cessação de kamma.” [SN.III.132](<a
href="../sutta/SNXXXV.146.php">SN XXXV.146</a>)<o:p></o:p></span></p>

<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-language:PT-BR'>* * *<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> “Bhikkhus, este corpo não lhes pertence, nem pertence a um outro. Vocês
devem vê-lo como kamma passado, formado por condições, nascido das volições, a
base para as sensações.” [SN.II.64] (<a href="../sutta/SNXII.37.php">SN XII.37</a>)<o:p></o:p></span></p>

<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-language:PT-BR'>* * *<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> "Bhikkhus, há três causas para a origem de kamma. Quais três? A cobiça é uma causa para a origem de kamma; a raiva é uma causa para a origem de kamma; a delusão é uma causa para a origem de kamma. Qualquer kamma, bhikkhus, criado através da cobiça, nascido da cobiça, causado pela cobiça, originado da cobiça, amadurece onde quer que renasça o ser/existir. Onde quer que o kamma amadureça, é ali que o resultado será experimentado, quer seja nesta vida, ou no próximo renascimento, ou em alguma ocasião subsequente. Qualquer kamma realizado por conta da raiva...por
conta da delusão...(igual à cobiça)<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> "Bhikkhus, há essas outras três causas para a origem de kamma. Quais três? A não-cobiça é uma causa para a origem de kamma; a não-raiva é uma causa para a origem de kamma; a não-delusão é uma causa para a origem de kamma. Qualquer kamma, bhikkhus, criado através da não-cobiça, nascido da não-cobiça, causado pela não-cobiça, originado da não-cobiça, é abandonado quando a cobiça desaparece; é cortado pela raiz, feito como com um tronco de palmeira, eliminado de tal forma que não mais estará sujeito a um futuro surgimento. Qualquer kamma
realizado por conta da não-raiva...por conta da não delusão...” [AN.I.134] (<a href="../sutta/ANIII.34.php">AN III.34</a>)<o:p></o:p></span></p>

<p class=Normal align=center style='text-align:center'><span lang=PT-BR
