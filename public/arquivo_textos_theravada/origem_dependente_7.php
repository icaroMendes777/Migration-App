<p class=Tit1 align=center style='text-align:center'>7. A Origem Dependente na Sociedade</Tit1>

<p class=Normal>&nbsp;</p>

<hr size=2 width="100%" align=center>

<p class=Normal>&nbsp;</p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O sutta
mais longo do Cânone em Pali que trata da Origem Dependente é o <a
href="../sutta/DN15.php">Mahanidana Sutta [DN.II.55-71] (DN 15)</a> . Nesse
sutta o Buda explica o princípio da condicionalidade tanto sob a perspectiva do
indivíduo, como ele ocorre dentro da mente e também no contexto social, como
ele ocorre nas relações humanas. Até agora lidamos exclusivamente com o
princípio da Origem Dependente tal como ele ocorre na consciência humana
individual. Antes de deixar este tema seria portanto apropriado mencionar
brevemente como a Origem Dependente opera na escala social.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O ciclo da
Origem Dependente descreve o surgimento dos padecimentos sociais seguindo o
mesmo esquema do surgimento do sofrimento no nível pessoal, exceto que a partir
do desejo ele diverge para uma descrição de eventos externos:<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Então, Ananda, na dependência do desejo surge o apego; na dependência
do apego surge a busca; na dependência da busca surge o ganho; na dependência
do ganho surge a decisão; na dependência da decisão surge o desejo e cobiça; na
dependência do desejo e cobiça surge o apego; na dependência do apego surge a
posse; na dependência da posse surge a mesquinharia; na dependência da
mesquinharia surge a salvaguarda; e devido à salvaguarda, muitos fenômenos
ruins e prejudiciais surgem – tomar clavas e armas, conflitos, brigas e
disputas, linguagem ofensiva, difamação e falsidades.” [<a href="#N19">19</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R19></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Abaixo segue uma comparação da forma como o princípio da Origem
Dependente opera no nível pessoal e social.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><i>Ignorância >>  Formações >> Consciência >> Mentalidade-materialidade (nome e forma) >> Bases dos Sentidos >> Contato >> Sensação >> Desejo >></i> <o:p></o:p></span></p>

<p class=Normal><b> Nível pessoal <o:p></o:p></span></b></p>


<p class=Normal style='margin-left:.75in'><i>Apego >> Nascimento >> Envelhecimento e morte, lamentação....sofrimento na vida</i></span></p>

<p class=Normal><b> Nível Social <o:p></o:p></span></b></p>

<p class=Normal style='margin-left:.75in'><i>Busca >> Ganho >> Decisão >>  
Desejo e Cobiça >> Apego >> Posse >> Mesquinharia >> Salvaguarda >>  Conflitos, disputas, brigas,linguagem ofensiva,difamação, falsidades...
sofrimento na sociedade</span></i><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Para
compreender a cadeia de eventos acima com mais clareza, vejamos alguns exemplos
descritos pelo Buda em outros textos, tal como o ciclo de <i>nanatta</i>
(diversidade), que pode ser resumido da seguinte forma:<o:p></o:p></span></p>

<p class=Normal><i><span lang=PT-BR style='mso-ansi-language:PT-BR'>Dhatunanatta</span></i><span
lang=PT-BR style='mso-ansi-language:PT-BR'> (diversidade dos elementos) =&gt; <i>phassananatta</i>
(diversidade dos contatos) =&gt; <i>vedanananatta</i> (diversidade das
sensações) =&gt; <i>saññananatta</i> (diversidade das percepções) =&gt; <i>sankappananatta</i>
(diversidade dos pensamentos) =&gt; <i>chandananatta</i> (diversidade dos
desejos) =&gt; <i>parilahananatta</i> (diversidade das agitações) =&gt; <i>pariyesananatta</i>
(diversidade da busca) =&gt; <i>labhananatta</i> (diversidade do ganho). [<a
href="#N20">20</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R20></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>A primeira seção, de <i>dhatu</i> até <i>sañña</i>, pode ser re-fraseada
simplesmente assim: devido à múltipla proliferação dos elementos, surge a
múltipla proliferação das percepções. Em um outro texto do Cânone em Pali a
seguinte seqüência de eventos é descrita:<o:p></o:p></span></p>

<p class=Normal><i><span lang=PT-BR style='mso-ansi-language:PT-BR'>Dhatunanatta</span></i><span
lang=PT-BR style='mso-ansi-language:PT-BR'> (diversidade dos elementos) =&gt; <i>saññnanatta</i>
(diversidade das percepções) =&gt; <i>sankappananatta</i> (diversidade dos
pensamentos) =&gt; <i>phassananatta</i> (diversidade dos contatos) =&gt; <i>vedanananatta</i>
(diversidade das sensações) =&gt; <i>chandananatta</i> (diversidade dos desejos)
=&gt; <i>parilahananatta</i> (diversidade das agitações) =&gt; <i>pariyesananatta</i>
(diversidade da busca) =&gt; <i>labhananatta</i> (diversidade do ganho).
[SN.II.140-149] (SN XIV.1-10)<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Essa
seqüência demonstra um processo que conecta a experiência mental individual com
os eventos externos, mostrando como a origem dos problemas sociais e do
sofrimento se encontram nas impurezas da mente humana. A seqüência é bastante
elementar, mostrando apenas um esboço do desdobramento dos eventos. Explicações
mais detalhadas, enfatizando situações mais específicas aparecem em outros
Suttas, tal como no Aggañña (<a href="../sutta/DN27.php">DN 27</a>),
no Cakkavatti (<a href="../sutta/DN26.php">DN 26</a>) e no
Vasettha (<a href="../sutta/MN98.php#T8">MN98</a>). Esses suttas são os modelos de como a Origem Dependente
opera no nível social. Eles explicam o desenvolvimento dos eventos na sociedade
humana, tal como o surgimento das estruturas de castas, como resultado da
interação entre as pessoas e o ambiente à sua volta. Em outras palavras, esses
fenômenos são o resultado de uma interação em três níveis: seres humanos,
sociedade humana e todo o ambiente natural.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>As
sensações que experimentamos dependem do contato sensual, as quais, além dos
fatores internos existentes tal como a percepção, dependem de fatores sociais e
ambientais. Na dependência da sensação, surge o desejo, resultando na
diversidade de comportamentos humanos tanto em relação a outras pessoas bem
como em relação ao mundo à volta deles, limitados pelas restrições definidas
pelas circunstâncias sociais e ambientais. Os resultados dessas ações também
afetam todos os demais fatores. Os seres humanos não são os únicos fatores
determinantes no desenvolvimento social e ambiental, e o ambiente natural não é
o único fator determinante no condicionamento dos seres humanos ou da
sociedade. Todos eles constituem um processo de relações interdependentes.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Um trecho
do <a href="../sutta/DN27.php">Aggañña Sutta</a> demonstra a seqüência da
evolução social de acordo com causa e efeito da seguinte forma:<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>As pessoas
se tornam preguiçosas e começam a armazenar arroz (antes o arroz era abundante
e não havia necessidade de armazená-lo) e esta se torna a prática preferida
=&gt; as pessoas começam a armazenar suprimentos =&gt; pessoas inescrupulosas
começam a roubar dos outros para aumentar suas posses =&gt; terminando em
censura, mentira, punição =&gt; pessoas responsáveis, vendo a necessidade de
uma autoridade, nomeiam um rei =&gt; algumas das pessoas, desiludidas com a
sociedade, decidem dar fim às ações ruins e cultivar a prática da meditação.
Algumas delas vivem próximas das cidades e estudam e escrevem escrituras; elas
se tornam os Brâmanes. Aquelas que permanecem com suas famílias obtêm o seu
sustento por meio de várias profissões; elas se tornam os artesãos. O restante
das pessoas, incultas e incapazes, se torna a plebe. Dentre esses quatro grupos
um grupo menor se separa, abandonando a tradição e a vida em família e adotando
a ‘vida santa.’ Eles se tornam os samanas ou contemplativos.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O objetivo
desse Sutta é explicar o surgimento das várias classes sociais como um aspecto
do desenvolvimento natural baseado em causas associadas, não como mandamentos
de um Deus todo poderoso. Todas as pessoas são igualmente capazes de ter um
comportamento bom ou ruim e todas irão colher os resultados de acordo com a lei
natural; daí todos os seres serem igualmente capazes de alcançar a iluminação
se praticarem o Dhamma da forma correta.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O <a
href="../sutta/DN26.php">Cakkavatti Sutta</a> mostra o surgimento do crime e do
padecimento social de acordo com a seguinte seqüência de causa e efeito:<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>(O
governante) não comparte a riqueza com os pobres =&gt; abunda a pobreza =&gt;
abunda o roubo =&gt; abunda o uso de armas =&gt; abunda matar e mutilar =&gt;
abunda mentir =&gt; calúnia...atos sexuais impróprios...linguagem grosseira e
frívola...cobiça e raiva... entendimento incorreto =&gt; desejo pelo que é prejudicial,
cobiça, ensinamentos incorretos, desrespeito pelos pais, idosos e religiosos
=&gt;a longevidade e a aparência degeneram.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>É
interessante observar que nos tempos modernos, os esforços feitos para tentar
resolver os problemas sociais raramente estão sintonizados com as suas causas
reais. Eles buscam proporcionar expedientes provisórios, tal como estabelecer
aconselhamento para viciados em drogas e delinqüentes, mas não se investiga
profundamente, antes de mais nada,<span style="mso-spacerun: yes">  </span>as
condições sociais que influenciam o surgimento desses problemas, tais como o
consumismo e os meios de comunicação de massa. Nesse aspecto, o ensinamento Budista
da Origem Dependente no nível social oferece um precedente valioso para uma
inteligente e verdadeiramente eficaz análise e reforma social.<o:p></o:p></span></p>
