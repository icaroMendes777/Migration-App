<p class=Normal><span lang=PT-BR style='mso-bidi-font-size:10.0pt;mso-ansi-language:PT-BR'>Vazio
é uma modalidade de percepção, uma maneira de encarar as experiências. Ela não
adiciona nada e não retira nada dos dados brutos dos eventos físicos e mentais.
Você vê os eventos na mente e nos sentidos sem qualquer pensamento quanto à
existência de algo por detrás deles. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-bidi-font-size:10.0pt;mso-ansi-language:PT-BR'>Essa
modalidade é chamada de vazio porque ela é desprovida das pressuposições que
normalmente adicionamos às experiências para que elas façam sentido: as
estórias e idéias sobre o mundo que criamos para explicar quem somos e o mundo
no qual vivemos. Embora essas estórias e idéias tenham utilidade, o Buda
descobriu que algumas questões mais abstratas que surgem em decorrência delas -
sobre a nossa verdadeira identidade e a realidade do mundo exterior - desviam a
nossa atenção da experiência direta de como os eventos influenciam uns aos
outros no momento presente. Dessa forma, elas nos atrapalham quando tentamos
entender e solucionar o problema do sofrimento. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-bidi-font-size:10.0pt;mso-ansi-language:PT-BR'>Digamos
por exemplo, que você esteja meditando e apareça uma sensação de raiva em
relação à sua mãe. Imediatamente, a reação da mente é de identificar a raiva
como &quot;minha&quot; raiva, ou de dizer que &quot;eu estou&quot; com raiva.
Em seguida você elabora sobre a sensação, quer seja procurando encaixá-la dentro
do seu histórico de relacionamento com a sua mãe ou como parte das suas idéias
gerais sobre quando e onde a raiva em relação às nossas mães podem ser
justificadas. O problema com tudo isso, sob a perspectiva do Buda, é que essas
estórias e idéias causam muito sofrimento. Quanto mais você se envolve nelas,
mais você desvia a atenção de enxergar a verdadeira causa do sofrimento: os
rótulos de &quot;eu&quot; e &quot;meu&quot; que colocam todo o processo em
movimento. Como resultado, você não consegue encontrar a forma de desemaranhar
a causa e dar um fim ao sofrimento. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-bidi-font-size:10.0pt;mso-ansi-language:PT-BR'>Se,
no entanto, você conseguir adotar a modalidade vazio - não agindo ou reagindo
em relação à raiva, mas simplesmente observando-a como uma série de eventos em
si mesmos - você verá que a raiva está vazia de qualquer coisa com a qual valha
a pena se identificar ou possuir. A medida que você domine a modalidade vazio
com mais consistência, verá que essa verdade não se aplica somente a emoções
mais grosseiras como a raiva, mas também, até para os eventos mais sutis na
esfera da experiência. Esse é o sentido de vazio em todas as coisas. Quando
você enxergar isso, compreenderá que rótulos como &quot;eu&quot; e
&quot;meu&quot; são inadequados, desnecessários e que causam nada além de
estresse e sofrimento. Então você poderá abandoná-los. Quando você os abandonar
completamente, descobrirá uma forma de experiência ainda mais profunda que é
completamente livre. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-bidi-font-size:10.0pt;mso-ansi-language:PT-BR'>Para
obter domínio sobre a modalidade vazio de percepção é necessário treinamento em
sólida virtude, concentração e sabedoria. Sem esse treinamento, a mente tende a
ficar na modalidade que cria estórias e idéias sobre o mundo. E sob a
perspectiva dessa modalidade, o ensinamento sobre o vazio soa simplesmente como
mais uma estória ou idéia sobre o mundo, com novas regras. Em relação à estória
do seu relacionamento com a sua mãe, parece estar dizendo que na verdade não
existe uma mãe, nem você. Em relação às suas idéias sobre o mundo, parece estar
dizendo que, ou o mundo na verdade não existe, ou que o vazio é o grande
fundamento não diferenciado dos seres, do qual todos viemos e para o qual algum
dia todos retornaremos. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-bidi-font-size:10.0pt;mso-ansi-language:PT-BR'>Essas
interpretações não somente não atingem o significado de vazio como também
evitam que a mente adote a modalidade adequada. Se o mundo e as pessoas na
estória da sua vida realmente não existem, então todas as ações e reações nessa
estória são como uma matemática de zeros e você irá se perguntar se existe
alguma razão para a prática da virtude. Se, por outro lado, você vê o vazio
como o grande fundamento<span style="mso-spacerun: yes">  </span>não
diferenciado dos seres para o qual todos iremos retornar, então qual é a
necessidade para o treinamento da mente em concentração e sabedoria, já que
todos chegaremos lá de todo jeito? E mesmo se precisarmos de treinamento para
regressar ao nosso fundamento dos seres, o que irá evitar que escapemos dele e
soframos novamente? Portanto, em todas essas alternativas a idéia de treinar a
mente parece ser fútil e sem sentido. Ao focarmos na questão de se realmente
existe ou não algo por detrás das experiências, a mente fica tomada por assuntos
que impedem-na de penetrar na modalidade do momento presente. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-bidi-font-size:10.0pt;mso-ansi-language:PT-BR'>Agora,
as estórias e idéias sobre o mundo têm sua utilidade. O Buda as empregou para
ensinar as pessoas, mas ele nunca usou a palavra <i>vazio </i>quando falava
nessas modalidades. Ele contava a estória das vidas das pessoas para mostrar
como o sofrimento surgia das percepções inábeis que estavam por trás das suas
ações e como a libertação do sofrimento pode surgir sendo mais perceptivo. E
ele descreveu os princípios básicos subjacentes ao ciclo de renascimentos para
mostrar como as ações intencionais inábeis e prejudiciais conduzem à dor dentro
desse ciclo e como as hábeis e benéficas conduzem ao prazer, enquanto que as
ações deveras hábeis podem conduzir à completa superação do ciclo. Em todos esses
casos, os ensinamentos têm como objetivo fazer com que as pessoas foquem na
qualidade das percepções e intenções da mente no presente momento - em outras
palavras, fazer com que elas entrem na modalidade vazio. Tendo conseguido isso,
elas podem usar os ensinamentos sobre o vazio para o objetivo desejado:
afrouxar todos os apegos a idéias, estórias e pressuposições, deixando a mente
vazia de todo desejo, raiva e delusão e dessa forma vazia de sofrimento e
estresse. E no final das contas, esse é o vazio que realmente vale. <o:p></o:p></span></p>

