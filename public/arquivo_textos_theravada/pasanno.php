<p class=Tit1 align=center style='text-align:center'> Ajaan Pasanno </Tit1>

<p class=Normal>O Venerável Ajaan Pasanno (Reed Perry) nasceu em La Pas, Manitoba no Canadá. Ele estudou História na  Universidade de Winnipeg e depois de se formar ele passou algum tempo viajando pela Índia e de lá ele foi para a Tailândia. </p>

<p class=Normal>Em 1974 ele foi ordenado como bhikkhu no Wat Pleng Vipassana tendo o venerável Phra Khru Nanasirivatana como preceptor. Ele desenvolveu um genuíno interesse pela vida monástica e durante o primeiro ano como monge foi levado pelo seu professor para conhecer <a href="tradicao_de_florestas.php#Chah">Ajaan Chah</a>, com quem ele acabou permanecendo e desenvolvendo a sua prática. </p>
<p class=Normal>Ele foi um dos primeiros residentes no Wat Pah Nanachat, o monastério estabelecido por Ajaan Chah para monges ocidentais. O Venerável Pasanno se tornou o abade do Wat Pah Nanachat no seu sétimo ano de existência. Durante os 14 anos como abade, de 1982 a 1996, ele se tornou bastante conhecido na Tailândia e um professor do Dhamma muito respeitado. </p>

<p class=Normal>Em 1997 ele se mudou para a Califórnia, estabelecendo juntamente com <a
href="amaro_bhikkhu.php">Ajaan Amaro</a> o  Abhayagiri Monastery. Em 2010 Ajaan Amaro foi convidado para ser o abade do Amaravati Buddhist Monastery na Inglaterra. Desde então Ajaan Pasanno tem sido o único abade do Abhayagiri Forest Monastery. </p>

