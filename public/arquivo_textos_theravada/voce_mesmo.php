<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando fui
pela primeira vez estudar com o meu professor, Ajaan Fuang, ele me deu um
pequeno livreto com instruções de meditação e mandou que eu fosse para a colina
atrás do monastério para meditar. O livreto – escrito pelo mestre dele, Ajaan
Lee – começava com uma técnica de meditação com a respiração e concluía com uma
seção mostrando como a técnica era usada para induzir os primeiros quatro
níveis de jhana.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Nos anos
seguintes, eu vi Ajaan Fuang dando o mesmo livreto para cada um dos seus novos
praticantes, leigos e ordenados. No entanto, apesar do livreto conter
descrições detalhadas dos jhanas, ele mesmo raramente mencionava a palavra
jhana nas suas conversas e nunca indicava a qualquer um dos seus praticantes
que eles haviam alcançado um determinado nível de jhana nas suas práticas.
Quando um praticante relatava uma experiência meditativa que se repetia, ele
gostava de discutir não o que aquilo era mas o que fazer com aquilo: no que
focar, o que deixar de lado, o que mudar, o que manter inalterado. Então, ele
ensinava como fazer experimentos com aquilo – tornar aquilo ainda mais estável
e pacífico – e como julgar os resultados dos experimentos. Se os praticantes
dele quisessem medir o seu progresso contra as descrições de jhana do livreto,
isso era problema deles e ele não tinha nada que ver com isso. Ele nunca disse
isso de um modo expresso, mas dada a forma como ele ensinava, a mensagem
implícita era muito clara. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>E claros
eram também os motivos implícitos para a atitude dele. Certa vez, ele me contou
a sua própria experiência quando era um jovem meditador: “Naqueles tempos não
tínhamos livros que explicavam tudo, do jeito que temos hoje. Quando comecei a
estudar com Ajaan Lee, ele me disse para trazer a minha mente para baixo.
Então, eu me concentrei para abaixá-la, abaixá-la, abaixá-la, mas quanto mais
eu a abaixava, mais pesada e embotada ela ficava. Eu pensei, ‘Isso não pode
estar certo.’ Então, eu voltei ao ponto de partida e passei a me concentrar
para elevá-la, elevá-la, elevá-la, até que encontrei um ponto de equilíbrio e
pude entender a que ele estava se referindo.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Esse
incidente foi um dos muitos que ensinaram importantes lições para Ajaan Fuang:
você precisa testar as coisas por você mesmo, para ver onde as instruções devem
ser tomadas literalmente e onde elas devem ser tomadas com sentido figurado;
você tem de julgar por si mesmo quão boa está a sua prática; e você tem de ser
engenhoso, experimentando e se arriscando para descobrir os meios para lidar
com os problemas à medida que eles surgirem.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, como
professor, Ajaan Fuang tentava incutir nos seus praticantes essas mesmas
qualidades de auto-confiança, engenhosidade e a disposição para se arriscar e
testar as coisas por eles mesmos. Ele fazia isso não só através de narrativas
sobre essas qualidades, mas também colocando-o em situações em que você tinha
de desenvolvê-las. Se ele sempre estivesse disponível para confirmar que “Sim,
você alcançou o terceiro jhana,” ou “Não, isso é apenas o segundo jhana,” ele
estaria impedindo o desenvolvimento das qualidades que ele estava tentando
infundir. Ele, ao invés dos seus próprios poderes de observação, estaria sendo
a autoridade sobre o que estava ocorrendo na sua mente, e você teria sido
absolvido de qualquer responsabilidade de avaliar corretamente aquilo que havia
experimentado. Como certa vez ele me disse, “Se eu tiver de explicar tudo, você
irá se acostumar a receber as coisas numa bandeja. E depois, sem nenhuma
experiência para entender as coisas por si mesmo, o que você fará quando
surgirem problemas na sua meditação?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então,
estudando com ele, tive de aprender a correr riscos no meio das incertezas. Se
algo interessante surgisse na minha prática, eu tinha de permanecer com aquilo,
observar aquilo ao longo do tempo, antes de chegar a alguma conclusão a
respeito. E assim mesmo aprendi, os rótulos que eu aplicava às minhas
experiências não podiam ser esculpidos em pedra. Eles tinham de ser mais como
notas num post-it, marcadores convenientes para a minha própria referência, que
eu talvez tivesse de descolar e grudar em algum outro lugar quando eu me
familiarizasse com o território da minha própria mente. E isso resultou numa
lição valiosa, aplicável a todas as áreas da minha prática. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Apesar
disso, Ajaan Fuang não deixou a reinvenção da roda do dharma totalmente por
minha conta. A experiência havia mostrado para ele que algumas abordagens, em
relação à concentração, funcionavam melhor do que outras para colocar a mente
numa posição, onde ela poderia exercer a sua engenhosidade e julgar com
precisão os resultados dos seus experimentos. Ele era bastante explícito ao
recomendar essas abordagens. Dentre os pontos que ele enfatizava estavam os
seguintes: <o:p></o:p></span></p>

<p class=Normal><i><span lang=PT-BR style='mso-ansi-language:PT-BR'>Uma
forte concentração é absolutamente necessária para o insight libertador. </span></i><span
lang=PT-BR style='mso-ansi-language:PT-BR'><span style="mso-spacerun:
yes"> </span>“Sem uma base firme na concentração,” Ajaan Fuang dizia sempre, “o
insight é simplesmente uma porção de conceitos. Para ver com clareza as
conexões entre o sofrimento e as suas causas, a mente tem que estar muito
estável e tranqüila. E para que fique tranqüila, ela necessita da intensa
sensação de bem-estar que só uma forte concentração pode proporcionar.”<o:p></o:p></span></p>

<p class=Normal><i><span lang=PT-BR style='mso-ansi-language:PT-BR'>Para
obter o insight num estado de concentração, você tem de permanecer nele por
muito tempo. </span></i><span lang=PT-BR style='mso-ansi-language:PT-BR'>Se
você empurrar com impaciência de um nível de concentração para o seguinte ou se
tentar analisar um novo estado de concentração demasiado rápido, depois de
tê-lo atingido, você não lhe dará oportunidade para mostrar todo o seu
potencial e não dará a si mesmo a oportunidade para se familiarizar com ele.
Então, você tem que<span style="mso-spacerun: yes">  </span>continuar
trabalhando nisso como se fosse uma habilidade, algo que você possa acessar em
todas as situações. Isso o capacitará a ver esse nível de concentração sob
perspectivas variadas e testá-lo ao longo do tempo, para ver se ele realmente é
completamente bem-aventurado, vazio e desprovido de esforço, como pode ter
parecido à primeira vista. <o:p></o:p></span></p>

<p class=Normal><i><span lang=PT-BR style='mso-ansi-language:PT-BR'>O melhor
estado de concentração para desenvolver o insight abrangente<span
style="mso-spacerun: yes">  </span>é aquele que inclui a consciência em relação
ao corpo todo. </span></i><span lang=PT-BR style='mso-ansi-language:PT-BR'>Havia
duas exceções à prática usual de Ajaan Fuang, de não identificar o estado que havia
sido alcançado na sua prática, e ambas envolvem os estados de concentração
incorreta. A primeira é o estado que surge quando a respiração se torna tão
confortável que o seu foco deriva da respiração para a própria sensação de
conforto, a sua atenção plena começa a nublar e a sua noção do corpo e do que
está à sua volta se perde numa bruma prazerosa. Quando você emerge, é difícil
identificar exatamente no que estava focando. Ajaan Fuang chamava isso de<span
style="mso-spacerun: yes">  </span><i>moha-samadhi</i>, ou “concentração
deludida.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O segundo
estado foi um que experimentei certa noite quando a minha concentração estava
extremamente unificada e tão refinada que se recusava a se estabelecer ou
rotular até mesmo os mais fugazes objetos mentais. Eu acabei caindo num estado
no qual perdi toda a noção do corpo, de todos os sons internos e externos, de
todos os pensamentos e percepções – embora ainda houvesse um mínimo de consciência
para que soubesse, quando emergi, que eu não havia estado adormecido. Descobri
que podia ficar nesse estado durante muitas horas e no entanto parecia que o
tempo voava. Duas horas pareciam dois minutos. Eu também podia “programar-me”
para sair desse estado depois de um tempo determinado. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Depois de
experimentar esse estado durante várias noites seguidas, eu relatei o ocorrido
para Ajaan Fuang, e a primeira pergunta dele foi, “Você gostou disso?” Minha resposta
foi não, porque me senti um tanto atarantado depois que emergi na primeira vez
. “Muito bem,” ele disse. “Enquanto você não gostar disso, você estará seguro.
Algumas pessoas realmente apreciam isso e pensam que é nibbana ou a cessação.
Na verdade é o estado de não percepção, <i>asañña-bhava</i>. Não é nem mesmo
concentração correta, porque não há meio nesse estado de investigar alguma
coisa para obter algum tipo de discernimento. Mas esse estado tem outros usos.”
Ele então me contou quando teve que se submeter a uma cirurgia nos rins e, não
confiando no anestesiologista, ele se colocou nesse estado durante a operação. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Nesses dois
estados de concentração incorreta é o escopo limitado da consciência que faz
com que eles sejam incorretos. Se grandes áreas da sua consciência estiverem
bloqueadas, como será possível desenvolver o insight abrangente?<span
style="mso-spacerun: yes">  </span>E como tenho notado em todos estes anos, as
pessoas adeptas da obliteração de grandes áreas da consciência, através de intensa
unicidade da mente, tendem a ser adeptos psicológicos da dissociação e da
negação. É por isso que Ajaan Fuang, seguindo Ajaan Lee, ensinou uma forma de
meditação da respiração que objetivava uma consciência abrangente da energia da
respiração por todo o corpo, fazendo várias tentativas com ela para obter uma
sensação de conforto e depois<span style="mso-spacerun: yes"> 
</span>acalmando-a para que ela não venha a interferir com a visão clara dos
movimentos sutis da mente. Essa consciência abrangente ajuda a eliminar os
pontos cegos nos quais a ignorância gosta de se ocultar. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Um estado
ideal de concentração que dê origem ao insight é aquele que pode ser analisado
como sofrimento, (dukkha), e ausência de sofrimento, mesmo enquanto você
estiver imerso naquilo. Uma vez que a sua mente estivesse firmemente
estabelecida num estado de concentração, Ajaan Fuang recomendava “elevá-la” do
seu objeto, mas não tão distante que a concentração fosse destruída. Dessa
perspectiva, você poderia avaliar quais níveis de sofrimento ainda estavam
presentes na concentração e abandoná-los. Nos estágios iniciais, isso em geral
envolvia ter de avaliar como você estava se relacionando com a respiração e
detectar níveis mais sutis de energia da respiração no corpo, que poderiam
proporcionar uma base para níveis mais profundos de calma. Uma vez que a respiração
estivesse perfeitamente tranqüila e a noção do corpo começasse a se dissolver
numa névoa sem forma, esse processo envolveria detectar as percepções de
“espaço,” “conhecer,” “unidade” e assim por diante, que apareceriam no lugar do
corpo e que poderiam ser descascados como as camadas de uma cebola na mente. Em
cada caso o padrão básico era o mesmo: detectar o nível de percepção ou
fabricação mental que estava causando o sofrimento desnecessário, deixar aquilo
de lado por um nível de percepção ou fabricação mais sutil até que não houvesse
nada mais para deixar de lado. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>É por isso
que, enquanto a sua consciência estivesse calma e alerta em todas as partes,
não importava se você estava no primeiro ou no último jhana, pois a maneira que
o estado de concentração era tratado era sempre a mesma. Dirigindo a sua
atenção para as questões do sofrimento e da sua ausência, Ajaan Fuang estava
direcionando você para os termos com os quais avaliar o seu estado mental por
si mesmo, sem ter de recorrer a uma autoridade externa. E, como resultado, os
termos que você pode avaliar por si mesmo – sofrimento, a sua causa, a sua
cessação e o caminho que conduz à sua cessação – são aquilo que definem as
Quatro Nobres Verdades. Como disse o Buda, quando você vê as coisas de acordo
com essas verdades, elas podem conduzí-lo à completa libertação. <o:p></o:p></span></p>


