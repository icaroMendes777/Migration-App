<p class=Tit1 align=center style='text-align:center'> Henri van Zeyst </Tit1>

<p class=Normal>Henri van Zeyst nasceu em Utrecht, na Holanda em 1908. Educado em escolas e numa universidade católicas, ele passou os seus últimos anos de estudo de filosofia e teologia e o seu primeiro ano, depois de ordenado padre, num monastério próximo a Florença na Itália. </p> 

<p class=Normal>Aos 31 anos de idade ele foi enviado para Londres a cargo de uma nova fundação da Ordem à qual pertencia. Durante a sua permanência em Londres ele também ensinou teologia dogmática para os escolásticos do Convento Christus ao norte de Londres. </p>

<p class=Normal>Um curso intensivo de religião comparada fez com que ele entrasse em contato como Budismo. Em menos de um ano depois da sua chegada ao Sri Lanka ele foi ordenado como monge Budista em 1938 com o nome de Bhikkhu Dhammapala. </p>

<p class=Normal>De 1956 a 1968 ele trabalhou na <i>Encyclopaedia of Buddhism</i>  na Universidade do Sri Lanka em Peradinyia. </p>

<p class=Normal>Ele é o autor de um grande número de livros publicados no Sri Lanka. </p>

