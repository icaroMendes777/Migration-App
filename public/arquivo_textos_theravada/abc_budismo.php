<p class=Tit2><a name=confiar></a><b>
Posso Confiar nos Suttas?</b> <a
href="#Top"><span style='text-decoration:none;text-underline:none'><img border=0 title="Topo" src="../img/tup.gif"></span></a></p>
<a name=Top></a>
<br>

<p class=Normal>O registro dos ensinamentos do Buda podem ser encontrados nos Suttas que são discursos proferidos pelo próprio Buda, ou pelos seus discípulos mais próximos. São cerca de 11.500 discursos. O número de Suttas pode apresentar alguma variação dependendo da forma como alguns Suttas são agrupados ou divididos.</p>

<p class=Normal>Há duas coleções de Suttas que são reconhecidas por estudiosos e acadêmicos como contendo os ensinamentos originais do Budismo. Essas coleções são os <a href="../sutta_pitaka.php">Nikayas</a> em Pali e os Agamas em Chinês. Os demais Suttas, principalmente aqueles que fazem parte da tradição Mahayana, são reconhecidos por estudiosos e acadêmicos como tendo sido compostos muito tempo depois do falecimento do Buda e portanto não fazem parte do conjunto original de ensinamentos. </p>

<p class=Normal>A questão que pode surgir na mente das pessoas é se esses ensinamentos originais são de fato originais e se são confiáveis, no sentido de expressarem aquilo que de fato foi ensinado pelo Buda. </p>

<p class=Normal>Há vários argumentos que respondem positivamente a essas questões, ou seja, que de fato os Suttas originais são confiáveis e expressam aquilo que o Buda ensinou. Os principais argumentos são os seguintes:</p>

<p class=Normal><b>1. O exemplo vivo da Sangha</b></p>

<p class=Normal>Em geral os Suttas mencionam explicitamente que o texto foi dito pelo próprio Buda ou em outros casos pelos seus discípulos mais próximos. </p>

<p class=Normal>Além dos Suttas o Buda também estabeleceu o Vinaya que é o conjunto das regras monásticas. A <a href="../caminho_liberdade/sangha.php#">Sangha</a>, ou a comunidade de monges e monjas, é bastante variada nos diversos países nos quais o Budismo é praticado. Variada no sentido de adotar com mais rigor ou menos rigor as regras definidas no Vinaya. O Buda não fez exceções em termos de adoção ou não dessas regras. Quem quer fazer parte da Sangha estabelecida pelo Buda adota as regras do Vinaya integralmente. Mas a realidade é que existem muitas pessoas que se apresentam como monges/monjas, vestem os mantos característicos, mas não seguem as regras com rigor.</p>

<p class=Normal>Nos países típicos do Budismo Theravada há monges que fazem parte daquilo que é conhecido como "tradição das florestas", que no geral seguem os ensinamentos dos Suttas e do Vinaya com rigor. No Ocidente, bem como na própria Tailândia, é bem conhecida a tradição das florestas do falecido mestre <a href="../arquivo_textos_theravada/tradicao_de_florestas.php#Chah">Ajaan Chah</a>. Nessa tradição pode ser encontrado o exemplo vivo da Sangha ou seja monges que vivem e praticam de acordo com aquilo que foi ensinado pelo Buda há mais de 2.500 anos, de acordo com os Suttas e o Vinaya. Conhecendo e convivendo com os monges dessa tradição é possível confirmar que os ensinamentos contidos nos Suttas e no Vinaya conduzem à realização assegurada pelo Buda, expressa na completa libertação da mente.</p>

<p class=Normal>Esse exemplo vivo da Sangha assegura que os ensinamentos aos quais temos acesso hoje são autênticos pois cumprem o que prometem. Esse argumento por si só já deveria ser suficiente para convencer qualquer um que os ensinamentos dos Suttas são confiáveis e funcionam. Mas como poucas pessoas terão a oportunidade de conhecer e conviver com esses monges iluminados há outros argumentos que podem ajudar a depositar confiança nos Suttas.</p>

<p class=Normal><b>2. Primeiro Concílio</b></p>

<p class=Normal>De acordo com os registros históricos, alguns meses após a morte do Buda houve um primeiro concílio (<i>sangiti</i>) com a participação de 500 bhikkhus arahants no qual foram recitados e acordados os ensinamentos dos Suttas e as regras do Vinaya. Esse passou a ser o registro oficial dos ensinamentos do Buda. </p>

<p class=Normal><b>3. Tradição Oral</b></p>

<p class=Normal>Durante cerca de 500 anos os ensinamentos que foram acordados no primeiro concílio foram preservados apenas através da tradição oral. Esse é um ponto que levanta muitas suspeitas na mente de muitas pessoas quanto à autenticidade dos Suttas pois todos sabem que a memória está sujeita a falhas, conforme já foi demonstrado em vários tipos de experimentos. No entanto, há argumentos em favor da fidelidade da transmissão oral:</p>

<p class=Normal style='margin-left:.75in'>- A Índia mantém até hoje uma forte tradição oral que teve origem, muito antes da época do Buda, com a preservação dos ensinamentos dos Vedas dos Brâmanes. Portanto já na época do Buda a tradição da transmissão oral já existia com pessoas sendo treinadas nesse tipo de habilidade. Os editais gravados em rocha do imperador Ashoka indicam que existia a linguagem escrita, no entanto para a preservação de ensinamentos tão preciosos como os Upanishads dos Brâmanes e os Suttas do Buda, a tradição oral era considerada como mais confiável.</p>

<p class=Normal style='margin-left:.75in'>- Os Suttas devem ter sido discursados e provavelmente foram editados de tal forma a facilitar a memorização. Por exemplo com o uso de sinônimos, de símiles, padronização do vocabulário e frases. A estrutura dos textos que para nós hoje é repetitiva e algumas vezes enfadonha, teve como propósito facilitar a memorização. É possível supor que o Buda tenha empregado recursos mnemônicos nos seus discursos, mas também podemos imaginar que os editores dos Suttas, provavelmente na sequência do primeiro concílio, tenham adotado mais recursos, como por exemplo a padronização de trechos, também com o propósito de facilitar a memorização dos textos. </p>

<p class=Normal style='margin-left:.75in'>- Em Myanmar na atualidade há monges capazes de memorizar e recitar todos os Suttas da completa coleção dos Nikayas provando que essa não é uma tarefa impossível.</p>

<p class=Normal style='margin-left:.75in'>- Quem tiver a curiosidade de conhecer um pouco mais sobre como expandir e dar mais confiabilidade à capacidade de memorização recomendo ler os livros "Moonwalking with Einstein" (Joshua Foer) e "The Memory Book" (Harry Loraine & Jerry Lucas). </p>

<p class=Normal><b> 4. Registros por Escrito</b></p>

<p class=Normal> Os Suttas foram registrados na forma escrita pela primeira vez no Sri Lanka antes do início da era Cristã. Havia uma situação de conflitos e fome no país e um grande risco da perda dos ensinamentos. Desde então ambos sistemas - o registro escrito e a tradição de memorização dos ensinamentos têm sido preservados.</p>

<p class=Normal><b>5. Harmonia Interna</b></p>

<p class=Normal>Aqueles que já tiveram a oportunidade de ler muitos, ou mesmo poucos Suttas, devem ter notado a coerência ou harmonia interna dos Suttas. Em outras palavras, que numa coleção tão vasta de ensinamentos as idéias apresentadas são absolutamente coerentes entre si, sem contradições ou idéias conflitantes, que à primeira vista poderia ser esperado de um conjunto tão amplo. Essa harmonia interna dos Suttas é um forte indício de que esses ensinamentos tiveram uma única origem pois se tivessem sido compostos ao longo do tempo por distintas pessoas não seria surpreendente encontrar contradições e idéias conflitantes, aliás tal como ocorre na literatura Mahayana. </p>


<p class=Normal> <b>6. Nikayas, Agamas e Ashoka</b></p>

<p class=Normal> No ápice do seu governo, por volta do ano 250 antes da era Cristã, o império de Ashoka se estendia pela quase totalidade do sub-continente Indiano até Kandahar no atual Afeganistão e o Himalaia ao norte. Ashoka é creditado pela transformação do Budismo de mais uma das várias religiões da índia  numa das principais religiões mundiais. </p>

<p class=Normal> O elemento mais importante com relação a Ashoka, para responder a questão da confiabilidade dos suttas, é que ele patrocinou um Concílio da Sangha Budista na sua capital em Pataliputra por volta do ano 250 antes da era Cristã. A importância desse Concílio foi a unificação da Sangha, com a exclusão de elementos que não se adequavam ao Vinaya e a unificação dos ensinamentos com a refutação das doutrinas conflitantes com os ensinamentos originais. Na sequência Ashoka enviou monges emissários para distintas regiões dentro e fora do seu império, destacando-se o Sri Lanka, no qual Mahinda, filho de Ashoka, transmitiu os ensinamentos do Buda ao rei Mogaliputta Tissa que acabou adotando o Budismo como religião oficial do estado, dando origem à tradição Theravada que até hoje continua florescendo no Sri Lanka.</p>

<p class=Normal> Também foram enviados emissários para o norte da Índia, na região da Caxemira, que mais tarde acabou sendo conhecida como a tradição Sarvastivada. Os ensinamentos da escola Sarvastivada acabaram encontrando o seu caminho para a China, onde foram preservados traduzidos para o Chinês.</p>

<p class=Normal> Portanto, cerca de 250 anos antes da era Cristã, entre 150 e 250 anos depois da morte do Buda (não há acordo com relação à data precisa), Ashoka enviou emissários com os ensinamentos do Buda para uma região no extremo norte do sub-continente Indiano e outra no extremo sul, na ilha do Sri Lanka, cerca de 3.000 km de distância uma da outra. Esses ensinamentos foram preservados, estudados, praticados e transmitidos de forma isolada em cada região e acabaram se convertendo no que hoje conhecemos como os Nikayas em Pali e os Agamas em Chinês. </p>

<p class=Normal> Desde meados do séc. XIX, e com maior intensidade nos últimos 20 anos, têm sido feitos estudos comparativos entre essas duas coleções e nesses estudos é que pode ser encontrada mais uma justificativa para a confiabilidade dos Suttas. Pois em termos doutrinários esses estudos confirmam que há sintonia entre os ensinamentos das duas coleções, ou seja ambas coleções apresentam perfeita coerência em termos dos ensinamentos apresentados. Visto que ambas coleções foram transmitidas e preservadas em áreas geográficas muito distantes uma da outra, é possível afirmar que ambas coleções, dada a sua harmonia em termos doutrinários, possuem uma origem comum que só pode remontar ao próprio Buda.</p>

<p class=Normal> <b>Em resumo</b>, o exemplo vivo da Sangha combinado com a harmonia doutrinária interna na coleção dos Nikayas e dos Agamas, bem como nos estudos comparativos entre essas duas coleções, deveria ser suficiente para que qualquer um deposite confiança nos ensinamentos contidos nos Suttas.</p>

<p class=Normal>Os questionamentos que existem hoje em relação à confiabilidade da memória é um fenômeno que provavelmente teve início com a disponibilidade de livros impressos, à partir do séc. XVI. Antes disso, todo conhecimento que alguém dispunha dependia da sua memória e havia métodos de treinamento para aumentar a capacidade de memorização e a sua confiabilidade.</p>

<p class=Normal>Quem estiver interessado numa análise mais profunda e extensa sobre a autenticidade dos ensinamentos originais do Buda, recomendo a leitura do livro: <i>The Authenticity of the Early Buddhist Texts</i>, escrito por Bhikkhu Sujato e Bhikkhu Brahamali.</p>



