<p class=Normal><b>Tomando como referência os <i>suttas</i> em pali dos<i> nikayas</i> podemos dizer que os Budistas acreditam no seguinte, procurando expressá-lo da forma mais resumida e compacta (se houver dúvidas - investigue, pergunte): </b></p>
</p>
<p class=Normal>O Buda de fato existiu, era uma pessoa como todos nós exceto por uma série de qualidades que fizeram dele uma pessoa extraordinária. </p>

<p class=Normal>A mente vem primeiro, a mente determina a nossa qualidade de vida. </p>

<p class=Normal>A vida nos oferece muitas alegrias, prazeres e felicidade. </p>

<p class=Normal>Na mesma medida, a vida também nos oferece muitas tristezas, dores e sofrimento. </p>

<p class=Normal>A generosidade, compaixão, benevolência, boa vontade, diligência, atenção, são qualidades benéficas que devem ser cultivadas. </p>

<p class=Normal>A cobiça, avareza, crueldade, má-vontade, raiva, negligência, são qualidades prejudiciais que devem ser evitadas. </p>

<p class=Normal>O renascimento ocorre. Esta não é a única vida, nem será a última. O ciclo de renascimentos é chamado <i>samsara</i>.</p>

<p class=Normal>O renascimento pode ocorrer em vários tipos de mundos, incluindo seres humanos, animais, inferno e divindades. </p>

<p class=Normal>Todo renascimento é temporário, não existe nenhum mundo em que o renascimento seja permanente. </p>

<p class=Normal><i>Karma</i> é ação intencional. Uma ação sem intenção não é <i>karma</i>.</p>

<p class=Normal>Todo <i>karma</i>, ou ação intencional, produz frutos, ou <i>vipaka</i>. Os frutos podem ser colhidos de imediato, mais tarde nesta vida, ou em alguma outra vida. </p>

<p class=Normal>Os frutos de <i>karma</i>, ou <i>vipaka</i>, em grande parte determinam as nossas experiências de vida, as sensações que marcam essas experiências. </p>

<p class=Normal><i>Karma</i> também determina o renascimento: em qual mundo e em quais condições ocorrerá o renascimento. </p>

<p class=Normal>O núcleo dos ensinamentos do Buda são as Quatro Nobres Verdades que tradicionalmente são formuladas como: (1) o sofrimento, (2) a origem do sofrimento, (3) a cessação do sofrimento e (4) o caminho que conduz à cessação do sofrimento. Alternativamente as Quatro Nobres Verdades podem ser formuladas como: (1) a verdadeira felicidade existe, (2) o seu oposto - a infelicidade, (3) os impedimentos para a verdadeira felicidade, e (4) o caminho para encontrar a verdadeira felicidade.

<p class=Normal>Não há nada que seja permanente. Todos fenômenos mentais e materiais são impermanentes, incertos, instáveis. </p>

<p class=Normal>O surgimento e desaparecimento dos fenômenos ocorre devido a condições. Por exemplo, o envelhecimento e a morte só ocorrem se há nascimento. Portanto o nascimento é uma condição para o envelhecimento e a morte. Se não houver nascimento, não há envelhecimento e morte.</p>

<p class=Normal>A impermanência e a condicionalidade dos fenômenos são elementos essenciais dos ensinamentos do Buda, estando inseridas na segunda e na terceira Nobres Verdades. </p>
