<p class=Tit2><a name=deus></a>Os Budistas Acreditam em Deus? <a
href="#Top"><span style='text-decoration:none;text-underline:none'><img border=0 title="Topo" src="../img/tup.gif"></span></a></p>
<a name=Top></a>
<br>

<p class=Normal>Na cosmologia do Budismo Theravada há <a
     href="../caminho_liberdade/mundos.php">31 mundos de existência</a> dos quais 26 são habitados por divindades, (ou devas). 
Os 5 mundos restantes correspondem ao inferno, mundo animal, fantasmas famintos, titãs e seres humanos. 
O nascimento nesses 31 mundos é temporário e não há nenhum mundo onde a existência seja permanente ou eterna. 
Todos os seres em todos os mundos estão sujeitos ao falecimento e ao renascimento no mesmo mundo ou em algum 
outro. Os seres renascem em cada um desses mundos de acordo com o seu karma. </p>

<p class=Normal>As divindades que renascem nos 26 mundos de existência possuem distintas características de refinamento ou pureza, 
por exemplo, os primeiros 6 mundos de divindades, depois do mundo humano, ainda fazem parte da esfera sensual, 
ou seja, são divindades que ainda desfrutam do prazer dos sentidos. A diferença em relação ao mundo humano é 
que essas divindades desfrutam mais prazeres do que os humanos, cuja experiência é uma combinação de prazer e 
dor. Já nos mundos seguintes as divindades apresentam gradualmente qualidades cada vez mais refinadas e purificadas, 
que correspondem aos estados mentais experimentados nos jhanas, que são estados mentais puros acompanhados de profunda 
concentração e absorção mental. Portanto, o que caracteriza as divindades de um determinado mundo são as qualidades 
mentais desenvolvidas e presentes na mente daquele ser. Essas qualidades mentais são provenientes da prática de ações 
meritórias ou da prática de desenvolvimento da mente através da meditação. Agora, através da prática dos <a
     href="../jhanas.php">jhanas</a>, é 
possível que alguns seres desenvolvam poderes extraordinários, os poderes supra-humanos descritos em vários suttas 
(veja a descrição completa dos <a
     href="../sutta/DN2.php#T87">poderes supra-humanos</a>). </p>

<p class=Normal>Os poderes supra-humanos descritos nos suttas não incluem a onisciência. A onisciência do Buda é um 
aspecto controverso no Cânone. Em geral o Cânone retrata o Buda sob uma perspectiva humanista, ele é um ser humano 
comum, igual a qualquer outro, que através do seu próprio esforço acabou descobrindo a realidade da vida. Essa 
descoberta, em muitos suttas, é descrita como a realização dos três conhecimentos verdadeiros: o conhecimento 
das vidas passadas, o conhecimento sobre como o karma determina o processo de renascimento e o conhecimento da 
destruição das impurezas mentais. Este último é o que caracteriza a iluminação e esse conhecimento não é privilégio 
do Buda, pois está disponível para qualquer um. Em contraponto a essa perspectiva humanista, os comentários da 
escola Theravada atribuem um certo grau de onisciência ao Buda. A posição ‘oficial’ da escola Theravada, é que o 
Buda era onisciente mas apenas em relação àquilo para o qual ele dirigia a sua mente, ou seja, o  Buda não era 
capaz de saber tudo de forma simultânea e precisava dar atenção para aquilo que ele queria saber (para mais detalhes 
veja o <a
     href="../sutta/MN71.php">MN 71</a> e o <a
     href="../sutta/MN12.php">MN 12</a>). Essa interpretação sofreu uma profunda transformação no Budismo, especialmente com o 
desenvolvimento do Mahayana, que atribui ao Buda um caráter totalmente transcendental, um caráter divino, distante 
do ideal humanista presente no Cânone. </p>

<p class=Normal>O capítulo a seguir - “Multiplicação de Budas e Bodisatvas” - foi traduzido de um ensaio de autoria 
do renomado estudioso do Budismo – Etienne Lamotte. Ele foi publicado no livro <i>The World of Buddhism</i> e descreve 
resumidamente o desenvolvimento deste tema no Budismo Mahayana. </p>

<p class=Normal><b>Multiplicação de Budas e Bodisatvas</b> </p>

<p class=Normal>"Mesmo reconhecendo no Buda uma série de poderes e prerrogativas, os <i>savakas</i>, (palavra usada para 
descrever os discípulos do Buda e o Theravada), durante muito tempo mantiveram-no no plano humano. Eles afirmavam 
que ele era “consumado no verdadeiro conhecimento e conduta, bem-aventurado, conhecedor dos mundos, um líder 
insuperável de pessoas preparadas para serem treinadas, mestre de devas e humanos,” mas não ignoravam o fato 
de que, uma vez morto, ele estaria invisível para devas e humanos, e não poderia fazer mais 
nada por eles abandonado-os à sua própria sorte, deixando, assim, o Dhamma como única herança. De acordo 
com eles, o surgimento de um Buda seria uma ocorrência muito rara  - tão rara quanto brotar uma flor numa 
figueira – e a humanidade permaneceria sem um guia e sem conselheiros durante longos períodos de tempo. </p> 

<p class=Normal>Um deus ‘morto a partir do seu <a
     href="../glossario.php#parinibbana"><i>parinibbana</i></a>’ – como definido por H. Kern – poderia bastar para 
os monges; mas não satisfaria as aspirações populares que clamavam urgentemente por um espírito superior, um 
panteão, santos, uma mitologia e um culto. A vulgarização da Boa Doutrina, (Dhamma), e a sua penetração nas 
massas teve o efeito de transformar o ‘mestre de devas e humanos’ num ‘Deus superior aos deuses,’ circundando-o 
com uma multitude de divindades menores e maiores bem como poderosos discípulos. As ‘seitas’ (<i>nikaya</i>) ou ‘escolas’ 
(<i>vada</i>) do chamado Hinayana já tinham dado o seu suporte para esse processo de sublimação: os Sarvastivadas estofando 
a lenda de Sakyamuni com milagres, os Mahasanghikas colocando a sua carreira histórica no campo dos fenômenos 
fictícios. Como a necessidade de protetores eficazes havia se tornado mais urgente, os <i>savakas</i> já tinham concebido 
ao lado do Buda histórico, um Messias compassivo, o futuro Buda Maitreya e alguns arahants imortalizados em benefício 
da causa, os quais estariam sempre dispostos a vir para ajudar os fiéis. </p> 

<p class=Normal>Esses desenvolvimentos permaneceram como casos isolados no Savakayana, mas os seguidores do Mahayana 
não tiveram nenhuma hesitação em aumentar infinitamente o número de Budas e grandes Bodisatvas. Arrebentando os limites 
estreitos da velha cosmologia, o Mahayana concebeu no coração do cosmo um considerável número de universos, cada um 
governado por um Buda assessorado por um, ou vários, grandes Bodisatvas. O Buda já possui a Perfeita Iluminação, enquanto 
que os grandes Bodisatvas – aqueles do décimo estágio – estão bem ‘próximos da Iluminação.’ Exceto por essa diferença, 
os Budas e Bodisatvas, inspirados pela mesma benevolência, convertem os seres nos seus universos e com freqüência 
aparecem simultaneamente em formas variadas e em distintos universos. </p> 

<p class=Normal>Sakyamuni, cuja existência histórica não pode ser duvidada, em pouco tempo veria um infinito número 
de pares e imitadores alinhados ao seu lado. Ele permaneceria como o mais conhecido dentre os Budas mas ele não seria 
mais o único. As escrituras do Mahayana mencionam muitos Budas e Bodisatvas até então desconhecidos.

<p class=Normal>Dentre esses Budas, são dignos de nota: Amitabha ou Amitayus, luminoso e com tempo de vida 
infinito, regente de Sukhavati, o Paraíso do Ocidente; Aksobhya, o imutável, localizado no leste, no universo 
Abhirati; Bhaisajyaguru, o mestre médico, que também habita o leste. Os mais famosos Bodisatvas são: Maitreya, 
aguardando no paraíso de Tusita para suceder a Sakyamuni; Avalokitesvara, que habitou o Monte Potalaka antes 
de manifestar-se na China sob a forma da divindade feminina Kuan-yin; Manjusri, de uma doce majestade, o 
Bodisatva da sabedoria." </p>

<p class=Normal><b>Conclusão:</b> </p>

<p class=Normal>É possível afirmar que, para aquelas pessoas com um entendimento mais profundo ou sofisticado 
dos ensinamentos Budistas, essa multitude de Budas e Bodisatvas são apenas manifestações da sabedoria e compaixão do Buda.</p> 
<p class=Normal>No entanto, parece impossível evitar que na cultura popular, menos esclarecida, os Budas e Bodisatvas acabem 
assumindo uma dimensão divina, que acaba caracterizando-os como Deuses Todo Poderosos, atribuindo a eles um caráter 
semelhante ao das religiões teístas. </p>
