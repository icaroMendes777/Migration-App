<p class=Normal><b>1. O Budismo é
uma doutrina com uma visão negativa da vida? O Budismo é niilista?</b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Não, o Budismo não tem uma visão negativa da vida e nem é niilista. Mas
é uma realidade que no Ocidente o Budismo tem sido rotulado como uma religião
negativa que enfatiza o sofrimento e a negação da vida, que defende o niilismo.
</span></p>



<p class=Normal><b>2. Porque o
Budismo recebeu esse rótulo negativo no Ocidente?</b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Essa idéia surgiu entre filósofos europeus durante o século 19. Na
época, vários escritores influentes, baseados em leituras falhas dos textos
Budistas, rotularam o Budismo como uma forma perigosa de niilismo. </span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Mais recentemente, esse tipo de idéia prevalece mesmo dentro da Igreja
Católica, como pode ser constatado no livro “Crossing the Threshold of Hope”,
escrito pelo Papa João Paulo II. Nesse livro, o Papa afirma que a iluminação do
Buda teve como base a convicção de que o mundo representa o mal e é a fonte de
sofrimento do homem. Dessa forma, para se livrar do sofrimento é necessário se
livrar do mundo. Seria necessário romper os vínculos que nos atam à realidade
externa. Quanto mais nos libertarmos desses vínculos mais nos libertaremos do
sofrimento, isto é, do mal que provém do mundo. </span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>O Buda nunca afirmou que o mundo é a fonte do mal e do sofrimento. A
ênfase do ensinamento Budista está na mente e não no mundo. O mundo é neutro –
é a mente do homem que cria dificuldades no mundo. A fonte do sofrimento, de
acordo com o ensinamento do Buda, é a cobiça e a raiva numa mente confusa.
Essas características não são do mundo, mas do próprio homem. </span></p>



<p class=Normal><b>3. Mas o que é o niilismo?</b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Niilismo é uma designação atribuída a várias filosofias radicais que
rejeitam todo e qualquer valor positivo e não acreditam em nada. Os niilistas
adotam um visão materialista da vida e afirmam que depois da morte não há nada.
Ou seja, com a morte, esse tipo de doutrina considera que não só o corpo é
aniquilado, mas a mente também é completamente aniquilada. </span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>O Budismo rejeita esse tipo de doutrina e afirma que após a morte há a
continuidade da consciência. Não no sentido de uma entidade permanente, como
por exemplo uma alma ou espírito, mas uma consciência que está sujeita a um
contínuo processo de vir a ser ou devir. Além disso, a doutrina de karma, ou
lei da natureza relativa às ações e suas conseqüências, que faz parte dos
ensinamentos centrais no Budismo, também é evidência de que o Budismo não
ensina a aniquilação completa com a morte. </span></p>



<p class=Normal><b>4. Mas o vazio ou vacuidade no Budismo não é uma afirmação niilista?</b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>O termo técnico Budista <i>shunyata</i> (em sânscrito) ou <i>suññata</i>
(em Pali) tem dado origem a mal-entendidos no Ocidente, onde esse termo é
interpretado como <i style='mso-bidi-font-style:normal'>o nada</i>, a ausência
ou extinção da existência. Shunyata, ou vacuidade, é um sinônimo para a origem
dependente. A origem dependente é provavelmente o ensinamento Budista mais
importante e em essência afirma que todas as coisas, quer sejam materiais ou
mentais, surgem, subsistem e desaparecem de acordo com causas e condições. </span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Nenhum tipo de coisa existe de forma autônoma, independente de causas e
condições. Essas causas e condições são internas e externas. Por exemplo, uma
árvore depende para sua sobrevivência do seu tronco, raízes, galhos e folhas,
que são as suas condições internas, mas também a árvore depende da terra, do
sol, do abastecimento de água e das condições climáticas de uma forma geral
para a sua sobrevivência, essas são as condições externas. Sem estar suportada
por essas condições a árvore não existe, o que leva à conclusão de que a árvore
em si é vazia. </span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Uma outra forma de análise é observar que as condições internas na
verdade são partes nas quais a árvore pode ser dividida e ao dividir a árvore
em partes, não há nada nessas partes que contenha em si a característica de
árvore. A idéia da árvore é um conceito criado pela mente a partir da união
daquelas partes e assim sendo a árvore em si é vazia. </span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Então, não é que o Budismo negue a existência das coisas, mas o que é
negado é que as coisas possuam algum tipo de existência independente, autônoma.
Se as coisas tivessem uma existência autônoma então o mundo seria estático,
permanente. É evidente que esse<span style='mso-spacerun:yes'>  </span>não é o
caso. Como as coisas são desprovidas de uma existência independente, autônoma,
e dependem de causas e condições para se manifestar, é dito que as coisas são
vazias, vazias de uma existência inerente. E justamente pelo fato das coisas
serem vazias que a mudança é possível. As coisas mudam quando as condições das
quais elas dependem mudam. Esse entendimento é o que viabiliza toda a prática
Budista pois o caminho Budista trata da mudança, da transformação dos estados
mentais inábeis, ou prejudiciais, em estados mentais hábeis, ou benéficos.<span
style='mso-spacerun:yes'>  </span></span></p>


<p class=Normal><b>5. O Budismo então também rejeita a eternidade?</b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>A idéia de uma vida eterna ou de coisas que durem para sempre é
rejeitada no Budismo. Se analisarmos as coisas neste mundo com atenção é
possível verificar que não há absolutamente nada que seja permanente. Todas as
coisas, quer sejam na nossa mente e corpo ou no exterior, exibem a mesma
característica de impermanência e instabilidade. As coisas surgem e desaparecem
de acordo com causas e condições. </span></p>



<p class=Normal><b>6. Mas e com relação ao sofrimento. Porque o Budismo coloca
<a name=PVW>tanta</a> ênfase no sofrimento?</b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>A visão Budista do mundo pode ser descrita como realista. Ninguém pode
negar que há sofrimento na vida. O envelhecimento, a enfermidade e a morte são
causas de sofrimento para a maioria das pessoas. Também no dia a dia as pessoas
estão sujeitas a todo tipo de desconfortos físicos e mentais que causam
sofrimento e estresse em graus variados. Na verdade, para a maioria das pessoas
a vida contém momentos de alegria e felicidade mais ou menos na mesma proporção
de momentos de tristeza e infelicidade. Poderíamos chamar isso de padrão de uma
vida normal e o Budismo simplesmente reconhece isso e afirma que não há nada de
errado nisso. Se as pessoas sofrem elas não precisam se culpar por isso, visto
