<p class=Tit2><a name=niilismo></a><span lang=PT-BR style='
mso-ansi-language:PT-BR'>O Budismo tem uma visão negativa da vida?<br> O Budismo é
niilista?<a
href="#Top"><span style='text-decoration:none;text-underline:none'><img border=0 title="Topo" src="../img/tup.gif"></span></a></p>
