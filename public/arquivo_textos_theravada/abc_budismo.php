<p class=Normal>A resposta é simples, para desfrutar da verdadeira felicidade.</p>

<p class=Normal>Todos nós sabemos o que é sentir-se feliz. Já sentimos isso várias vezes. O problema é que invariavelmente esse sentimento de felicidade não dura, acaba desaparecendo. Quantas vezes não desejamos obter algo acreditando que aquilo traria a verdadeira felicidade. Obtendo aquilo que desejamos, o sentimento de felicidade pode ser extremamente intenso e recompensador, mas passado algum tempo já não sentimos mais a mesma coisa e até começamos a ver defeitos naquilo que antes parecia completamente perfeito. A nossa reação, regra geral, é colocar  defeito no objeto. Ou seja, se aquilo que obtivemos não trouxe a felicidade completa e duradoura é porque não encontramos a coisa certa e assim saímos em busca de alguma outra coisa que seja perfeita e duradoura. E assim seguimos na nossa busca sem fim. </p>

<p class=Normal>Mas há uma felicidade que é perfeita e duradoura, que não desaparece e que não depende das circunstâncias. A verdadeira felicidade está no interior, nas nossas próprias mentes, não pode ser encontrada no mundo, lá fora. Essa felicidade não é egoísta pois não precisa tomar nada de ninguém e não causa nenhum tipo de dano a ninguém, pois se a nossa felicidade tiver que depender de tomar algo de outras pessoas ou do sofrimento de outras pessoas, elas de alguma forma irão tentar dar um fim nisso. A felicidade que vem do interior não precisa ter um fim e não precisa tomar nada de ninguém, sendo portanto um ato de sabedoria e um ato de compaixão. Para encontrá-la, o único método possível é a meditação. </p>

<p class=Normal><b>Entendendo a mente</b></p>

<p class=Normal>A meditação é como uma expedição exploratória no interior das nossas mentes. Um processo investigatório que tem dois objetivos. O primeiro é entender como a mente funciona e o segundo é treiná-la. </p>

<p class=Normal>Entender como a mente funciona significa compreender porque em determinadas situações acabamos agindo ou dizendo coisas que acabam ferindo até mesmo pessoas que queremos muito e a nós mesmos. Coisas das quais acabamos nos arrependendo mais tarde. Ou porque as coisas que obtemos e que nos trazem tanta felicidade, passado algum tempo perdem valor. </p> 

<p class=Normal>Treinar a mente significa deixar de lado aqueles hábitos mentais que acabam produzindo uma felicidade apenas temporária, que prejudicam os outros e a nós mesmos, e que nos afastam da verdadeira felicidade. Além disso, treinar a mente significa cultivar, desenvolver, aqueles hábitos que irão beneficiar os outros e a nós mesmos e que conduzem à verdadeira felicidade. </p>

<p class=Normal>O objetivo da meditação não é curar um determinado mal-estar emocional que a pessoa possa estar sentindo, mas sim atacar o problema na sua raiz eliminando as causas que dão origem a todos os tipos de mal-estar mental e que impedem a verdadeira felicidade. </p>

<p class=Normal><b>Quantos tipos de meditação existem?</b></p>

<p class=Normal>Existem vários tipos de meditação no Budismo, mas para atingir os objetivos descritos acima, há dois tipos de meditação que são os mais adequados. Uma é a meditação da concentração e a outra é a meditação de insight. Os dois tipos também são conhecidos como <i>samadhi</i> e <i>vipassana</i> em Pali (<i>shamatha</i> e <i>vipayshana</i> em Sânscrito). </p> 

<p class=Normal>A meditação da concentração visa acalmar e tranquilizar a mente enquanto que a meditação de insight visa ver as coisas que ocorrem na nossa mente como elas na verdade são. </p> 

<p class=Normal>Para dar um exemplo, imaginemos um lago com águas cristalinas, tão limpas que nos permita ver o fundo, ver as pedras e o cascalho, os peixes nadando. Agora imaginem a superfície do lago com ondas agitadas pelo vento, seria possível ver até o fundo do lago? Muito provavelmente não, a superfície do lago tem de estar calma, sem ondas, sem nenhuma agitação. Assim é como funciona a meditação da concentração, que tem como objetivo aquietar a agitação natural da mente para poder ver melhor o que está acontecendo. Agora imaginem que a superfície do lago está calma, a pessoa que está olhando é capaz de ver e apreciar aquilo que está na água, agora se essa pessoa tiver conhecimentos de biologia, geologia ou ecologia, ela poderá apreciar aquilo que está vendo num grau muito mais completo e profundo do que uma que não tenha esse tipo de conhecimento. De modo semelhante com a meditação de insight, que possibilita ao meditador enxergar e entender completa e profundamente aquilo que está ocorrendo na sua mente. Mas isso também implica que há um certo referencial para a prática da meditação de insight que são exatamente os ensinamentos do Buda sobre a realidade das coisas. Praticar a meditação de insight sem esse referencial é perda de tempo. Esse referencial pode ser obtido através do estudo dos ensinamentos Budistas. </p> 

<p class=Normal><b>Posso meditar sozinho?</b></p>

<p class=Normal>Não há nada que impeça alguém de começar a praticar meditação sozinho. É necessário ter um conhecimento razoável dos ensinamentos do Buda e com base nos textos disponíveis sobre meditação é possível ter uma idéia geral sobre como funciona a prática. Havendo oportunidade é recomendável que o meditador tenha o apoio de pessoas mais experientes e que participe de retiros de meditação para obter mais experiência e contar com o auxílio de um professor. </p> 

<p class=Normal>No entanto, há dois obstáculos que são enfrentados por todos os meditadores. Primeiro é a motivação e segundo, como lidar com as dificuldades que surgem. </p>

<p class=Normal>A meditação como qualquer arte ou esporte requer continuidade na prática. Certo maestro de uma orquestra sinfônica dizia que se um músico da sua orquestra ficasse três dias sem praticar, a audiência perceberia; se ele ficasse dois dias sem praticar, os seus colegas na orquestra perceberiam; e se ele ficasse um dia sem praticar, ele, maestro, perceberia. Com a meditação é a mesma coisa. É necessário incorporá-la como parte da rotina diária, assim como escovar os dentes ou ir ao banheiro. Para fazer isso, no entanto, é necessário motivação. Num retiro de meditação, onde há um grupo de pessoas e uma rotina de atividades estabelecida, fica mais fácil manter a disciplina. Em casa, sozinho, é muito mais difícil. A pessoa tem de encontrar dentro de si mesma essa motivação. Desenvolver um senso de urgência, (<a href="../glossario.php#samvega"><i>samvega</i></a> em Pali), é um dos principais fatores que ajuda a estimular a energia para a prática da meditação. Esse senso de urgência é despertado ao refletirmos sobre as inevitáveis vicissitudes da vida e o sofrimento gerado pelas enfermidades, envelhecimento e morte. O estudo, a leitura de textos Budistas pode servir como fonte de inspiração, e a participação num grupo que medite regularmente também pode ajudar. Ayya Khema dizia que as pessoas com tendência para aversão são aquelas que são motivadas com mais facilidade para a prática, porém são aquelas que provavelmente, pela própria aversão, irão encontrar mais dificuldades. </p> 

<p class=Normal>O segundo aspecto é como lidar com as dificuldades. Não que a meditação seja algo difícil. A meditação em si é muito fácil. As dificuldades são em geral criadas por nós mesmos devido aos hábitos mentais inábeis acumulados durante muito tempo. Isso é o que cria as dificuldades na meditação. </p>

<p class=Normal>Qualquer prática de meditação terá mais chance de ser bem sucedida se for acompanhada por uma sensação de bem-estar mental. Isso não só cria um fator de estímulo para praticar a meditação, como é também a condição necessária para que a meditação transcorra com menos dificuldades. O objeto de meditação mais recomendado é a respiração, que inclusive foi o que o próprio Buda empregou. A respiração tem a vantagem de ser algo neutro, mas ao mesmo tempo capaz de condicionar a mente e o corpo. Dependendo da forma como a respiração é observada, ou seja, o tipo de atenção e intenção na mente ao observar a respiração, combinado com um ritmo de respiração que seja confortável e agradável, é possível com facilidade criar essa sensação de bem-estar no corpo e na mente. Esse será um bom ponto de partida, ao qual poderemos sempre recorrer. </p>

<p class=Normal>Além disso é importante, como base para uma meditação bem sucedida, que o meditador observe certas regras de conduta, cujo conteúdo mínimo abarca os cinco preceitos e o ideal,  a Ação Correta, a Linguagem Correta e Modo de Vida Correto do <a href="../caminho_liberdade/dukkha_caminho.php">Nobre Caminho Óctuplo</a>. A observação dessas regras de conduta visa evitar o remorso e a agitação mental e com isso, contribuir para o ambiente de bem-estar e tranqüilidade da mente. </p>

<p class=Normal>Mas mesmo tomando todas essas precauções, é inevitável que as dificuldades surjam, e nesse aspecto, uma pessoa mais experiente pode ajudar bastante evitando que o meditador perca tempo em demasia com problemas de fácil solução. Por outro lado, o meditador tem de estar preparado para emoções fortes que podem se encontrar contidas dentro da mente e que devido à prática da meditação encontrem uma forma de vir à tona e assim impossibilitar que a mente se tranquilize. Nesse caso, o mais prudente é descontinuar a prática da meditação até que ele receba instrução adequada de como lidar com esse aspecto da prática, e para isso a ajuda de um meditador mais experiente ou de um professor será  necessária, pois se essa emoção for demasiado intensa e perturbadora, a prática da meditação poderá alimentá-la. </p> 

<p class=Normal>No entanto, há algumas dificuldades que o meditador irá enfrentar e para as quais ele mesmo terá de encontrar a solução, pois as experiências das pessoas variam muito e nem sempre um meditador mais experiente, ou um professor, terá uma resposta satisfatória. Mesmo que o professor tenha tido uma experiência semelhante, as soluções para as dificuldades na meditação podem variar de pessoa para pessoa. Nesses casos o meditador terá que agir como seu próprio professor, ser aluno e professor ao mesmo tempo. Para conseguir isso é necessário ser muito observador e estar disposto a fazer experimentos, tentar diferentes alternativas para avaliar o resultado, é dessa forma que o meditador irá conhecer melhor a sua mente. O importante é não ficar frustrado com as dificuldades que forem encontradas, lembrando sempre que a paciência e a equanimidade são qualidades mentais importantes e que são justamente as dificuldades que fazem com que elas amadureçam. </p>

<p class=Normal>Lembro de uma história do Ajaan Mun, um dos fundadores da <a href="../arquivo_textos_theravada/tradicao_de_florestas.php">tradição de florestas da Tailândia</a>, que no início da sua carreira como monge, perambulando pelas florestas da Tailândia, ao meditar, com a mente concentrada, tinha a visão de um cadáver e não sabia bem o que fazer com aquilo. Ele recorreu ao seu companheiro monge para pedir ajuda mas este foi incapaz de ajudá-lo, pois as suas experiências meditativas eram completamente distintas. Ajaan Mun teve de encontrar a resposta sozinho através da própria perspicácia. Através da sua determinação e esforço Ajaan Mun acabou se convertendo num dos mais renomados mestres de meditação na Tailândia no século XX. Para aqueles que o conheceram Ajaan Mun era um iluminado. A tradição que ele estabeleceu se mantém viva até os dias de hoje, não só na Tailândia, mas em vários monastérios estabelecidos pelos seus discípulos em muitos países da América do Norte, Europa e Oceania. </p>

