<p class=Tit2><a name=reencarnacao></a>O Budismo e a Reencarnação<a
href="#Top"><span style='text-decoration:none;text-underline:none'><img border=0 title="Topo" src="../img/tup.gif"></span></a></p>
<a name=Top></a>
<br>



<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:
PT-BR'>1. O Budismo acredita na reencarnação?</span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>O Budismo não ensina a reencarnação, o Budismo acredita no
renascimento.<span style='mso-spacerun:yes'>  </span></span></p>


<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2. Qual é a diferença entre reencarnação e renascimento?</span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>A reencarnação é a idéia da existência de um espírito separado do corpo;
com a morte do corpo esse mesmo espírito reassume uma outra forma material e
segue evoluindo. O renascimento na concepção Budista não é a transmigração de
um espírito, de uma identidade substancial, mas a continuidade de um processo,
um fluxo do devir, no qual vidas sucessivas estão conectadas umas às outras
através de causas e condições. Esse processo ou fluxo não ocorre apenas com a
morte mas está presente constantemente nas nossas vidas. Nós estamos em
constante mudança, com cada momento nas nossas vidas surgindo na dependência do
momento anterior, que deixou de existir. É um pouco parecido com a correnteza
de um rio, a correnteza fluindo continuamente sem cessar. Não é possível entrar
no mesmo rio duas vezes. </span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Podemos ilustrar o renascimento com um símile, é como se a chama de uma
vela fosse empregada para acender uma outra vela e nesse processo a primeira
vela fosse apagada. A chama da segunda vela surgiu na dependência da primeira
vela, ou seja, tem uma conexão com ela, mas a chama da segunda vela não é
idêntica à primeira. Então, as duas chamas possuem uma ligação mas não são
idênticas. </span></p>



<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:
PT-BR'>3. De onde então vem o homem e para onde ele está indo?</span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Há três respostas possíveis para esta questão. Aqueles que acreditam na
existência de um Deus, em geral, postulam que antes da criação de um ser ele
não existe, ele passa a existir pela vontade do Deus criador. De acordo com o
seu modo de vida, o seu destino será o paraíso ou o inferno eternos. Há outros,
humanistas e cientistas, que postulam que um ser surge através da concepção
baseada em causas naturais, nasce, e depois de viver algum tempo, morre
deixando de existir por completo. </span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>O Budismo não adota nenhuma dessas explicações. A primeira dá origem a
uma série de questões de ordem ética. É difícil explicar, se somos realmente
criados por um Deus, porque tantos seres nascem com deformidades terríveis ou
porque tantos fetos abortam por causas naturais ou são natimortos. Também
parece um tanto injusto que alguém esteja destinado ao sofrimento eterno no
inferno ou à felicidade eterna no paraíso tendo vivido apenas 60, 70 ou 80
anos. A segunda explicação é um pouco melhor do que a primeira e está mais
baseada em evidências científicas, mas ainda assim deixa muitas questões sem
resposta. Como é possível que um fenômeno tão incrível como a consciência possa
se desenvolver do simples encontro entre o esperma e o óvulo? E agora que
muitos fenômenos paranormais são reconhecidos como ramos da ciência, fenômenos
como a telepatia são cada vez mais difíceis de se encaixar num modelo puramente
materialista.</span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Para o Budismo, com a morte, a consciência com todas as suas tendências,
preferências, habilidades e características que foram desenvolvidas e
condicionadas nesta vida, se re-estabelece no embrião/feto. Dessa maneira, o ser cresce,
nasce e desenvolve uma personalidade condicionada pelas características que
foram trazidas da vida passada e pelo novo ambiente, além de outros fatores
condicionantes como a hereditariedade, etc. Essa personalidade está sujeita a
mudança e será modificada através do esforço consciente por fatores
condicionantes tais como a educação, a influência dos pais e da sociedade, etc.
Outra vez, com a morte, essa consciência irá se re-estabelecer num novo embrião/feto.
</span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Esse processo de renascimento irá continuar até que as condições que o
causarem persistam. Quando essas condições deixarem de existir, ao invés de
renascer, a consciência alcançará um estado que é chamado nirvana, e esse é o
objetivo último no Budismo.</span></p>



<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:
PT-BR'>4. Como a consciência migra de um corpo para outro?</span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Imagine as ondas de rádio. As ondas de rádio não são compostas de
palavras ou notas musicais mas de energia em distintas freqüências que são
transmitidas através do espaço e atraídas e capturadas por um receptor no qual
se manifestam como palavras e música. Algo similar ocorre com a consciência. Ao
morrer, a energia mental cruza o espaço e se une ao embrião/feto para
formar o novo ser. O embrião/feto e a consciência se desenvolvem através de uma
relação de mútua dependência e influência.<span style='mso-spacerun:yes'> 
</span></span></p>



<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:
PT-BR'>5. Os seres humanos sempre renascem como seres humanos?</span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Não. De acordo com o Budismo há vários planos de existência nos quais
ocorre o renascimento. Alguns seres renascem no paraíso celestial, alguns no
inferno e assim por diante. O paraíso celestial ou o inferno não são
propriamente lugares mas estados de existência onde a mente experimenta
respectivamente principalmente prazer ou dor. A vida nesses planos no entanto é
temporária e depois disso haverá um novo renascimento que poderá muito bem
ocorrer entre os seres humanos. Então, a principal diferença entre o plano
humano e os outros planos é a qualidade da experiência mental. </span></p>



<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:
PT-BR'>6. Qual o fator que decide onde um ser irá renascer?</span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>O fator mais importante que condiciona o renascimento é karma. </span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Karma quer dizer ação baseada na intenção e se refere ao conjunto de
ações com a mente, corpo e linguagem que constituem no seu conjunto a bagagem
que carregamos conosco. </span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Essas ações geram consequências que são os frutos do karma. Os frutos do
karma influenciam tanto a nossa experiência do mundo como o processo de
renascimento por ocasião da morte. </span></p>



<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:
PT-BR'>7. Mas o que exatamente é karma?</span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Karma é uma palavra em Sânscrito, (Kamma em Pali), que quer dizer ação
baseada na intenção e essa intenção inclui volição, escolha e decisão, o ímpeto
mental que conduz à ação. A intenção é aquilo que incita e dirige todas as
ações humanas, ambas, criativas e destrutivas e por isso é a essência de karma.
</span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Intenção no contexto Budista tem um significado muito mais sutil do que
o uso mais geral dessa palavra. Em geral tendemos a usá-la quando queremos
proporcionar um elo de ligação entre o pensamento interno e as suas ações
externas resultantes. Por exemplo, podemos dizer, “Eu não tinha intenção de
fazer isso,” “Eu não tinha intenção de dizer isso” ou “Ela fez isso de forma
intencional.”</span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Mas, de acordo com os ensinamentos Budistas, todas as ações e linguagem,
todos os pensamentos, não importa quão fugazes sejam, e as respostas da mente a
sensações recebidas através dos órgãos dos sentidos contêm elementos de
intenção. Assim, a intenção é a escolha volitiva feita pela mente em relação
aos objetos para os quais a atenção é dirigida; é o fator que conduz a mente a
se inclinar ou a repelir os vários objetos da atenção, ou de prosseguir em uma
certa direção; é o que guia ou governa como a mente responde aos estímulos; é a
força que planeja e organiza os movimentos da mente e no final das contas é
aquilo que determina os estados experimentados pela mente.</span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Karma opera no universo como uma cadeia contínua de causa e efeito. Essa
cadeia não está só confinada à causação no sentido físico, mas também tem
implicações éticas e morais. “Boas ações trazem bons resultados, más ações
trazem maus resultados”, é um dito comum. Nesse sentido karma é uma lei moral.</span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Os seres humanos estão constantemente emitindo energia física e mental
em todas as direções. Na física aprendemos que não há perda de energia, ela só
muda de forma. Essa é a chamada lei da conservação de energia. Do mesmo modo, a
energia mental nunca é perdida. Ela é transformada. Portanto, karma é a lei da
conservação da energia moral. </span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Através das ações com a mente, corpo e linguagem os seres estão emitindo
energia para o universo, e em contrapartida, eles são afetados pelas
influências que fluem na sua direção. Os seres portanto, enviam e recebem todas
essas influências, encontrando-se num estado de interdependência. </span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>O Karma não deve ser confundido com destino, fatalidade. Destino
transmite a idéia de que a vida de alguém foi planejada de antemão por algum poder
externo e que a pessoa não tem controle sobre o desenrolar dos eventos na sua
vida. </span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Nesse sentido é importante observar que karma através dos seus frutos é
um fator que influencia o futuro e não que determina o futuro, pois a cada
momento os seres têm a oportunidade de agir no sentido de reforçar os frutos do
karma ou de minimizá-los. Isso ocorre porque no Budismo karma não é visto de
uma forma absolutamente linear. Há um processo linear em operação através do
qual experimentamos no presente os frutos de ações passadas mas também há um
processo sincrônico no qual o presente é influenciado pelo fruto das ações no
presente. Dessa forma o Budismo reconhece que há um certo espaço para o
exercício do livre arbítrio. </span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Qualquer ação desprovida de intenção não tem impacto na lei de karma.
Por exemplo, um barranco desmoronando, uma pedra caindo de uma montanha, ou um
galho morto caindo de uma árvore, não faz parte do escopo da lei de karma, mas
de alguma outra lei da natureza. </span></p>

