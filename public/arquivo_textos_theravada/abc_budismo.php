<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:
PT-BR'>1. O que é o Budismo?</span></b>
</p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>A palavra Budismo é derivada da palavra ‘<i>bodhi</i>’ que significa
‘despertar’, portanto o Budismo é a filosofia do despertar ou iluminação. Essa
filosofia teve origem na experiência de um homem chamado Siddhata Gotama, conhecido
como o Buda, que realizou a iluminação por si próprio com 36 anos de idade. O
Budismo existe faz 2.500 anos e tem cerca de 300 milhões de adeptos no mundo
todo. Até meados do séc. XIX o Budismo era um filosofia com predominância na
Ásia mas desde então se expandiu para todo o mundo. </span></p>



<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2. Então o Budismo é apenas uma filosofia?</span></b><span lang=PT-BR
style='mso-ansi-language:PT-BR'></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>A palavra filosofia provém de duas palavras, ‘filo’ que significa ‘amor’
e ‘sofia’ que significa ‘sabedoria’. Então filosofia é o amor pela sabedoria ou
amor e sabedoria, ambos significados descrevem o Budismo com perfeição. O
Budismo ensina que devemos tentar desenvolver todo o potencial da nossa
capacidade mental de modo que possamos alcançar o claro entendimento da
realidade. O Budismo também prega o desenvolvimento do amor e bondade de modo
que possamos expressar verdadeira amizade por todos os seres. </span></p>



<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:
PT-BR'>3. Quem foi o Buda?</span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>No ano 560 antes de Cristo um bebê nasceu numa família real no norte da
Índia. Ele cresceu rodeado pela riqueza e pelo luxo mas acabou se dando conta
de que o conforto e a segurança mundanas não asseguram a felicidade.
Profundamente comovido pelo sofrimento que viu ao seu redor, ele decidiu tentar
encontrar o caminho para a felicidade humana. Com 29 anos de idade ele
abandonou a vida de príncipe e saiu em busca da solução para os problemas
humanos. Ele estudou e praticou com mestres distintos mas nenhum deles na
verdade sabia a causa do sofrimento humano e como superá-lo. Por fim, depois de
seis anos de estudos e experimentos com todos os tipos de práticas ascéticas e
meditativas, ele realizou o fim da ignorância e compreendeu a origem e a
cessação do sofrimento. A partir dessa data ele passou a ser o Buda, o
Iluminado. Ele viveu por mais 45 anos durante os quais viajou pelo norte da
Índia e ensinou a todos que quisessem ouvir aquilo que ele havia descoberto. A
sua paciência e compaixão eram legendárias e milhares de pessoas se tornaram
seus discípulos. Com oitenta anos ele faleceu. </span></p>



<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:
PT-BR'>4. Não foi um ato irresponsável do Buda abandonar a família? </span></b><span
lang=PT-BR style='mso-ansi-language:PT-BR'></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Não deve ter sido uma decisão fácil para Siddhata Gotama abandonar a
família em busca de algo completamente desconhecido. Durante muito tempo ele
deve ter sofrido com a angústia e hesitação. Ele tinha duas alternativas,
permanecer com a família ou dedicar-se ao mundo. No final a sua grande
compaixão prevaleceu e fez com que ele se entregasse ao mundo. E desde a sua
iluminação até os dias de hoje todo o mundo ainda se beneficia com o seu
sacrifício. Essa não foi uma decisão irresponsável mas talvez o supremo
sacrifício que alguém pode fazer. </span></p>



<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:
PT-BR'>5. O Buda está morto faz muito tempo, como ele pode nos ajudar?</span></b><span
lang=PT-BR style='mso-ansi-language:PT-BR'></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Faraday, descobriu a eletricidade e já morreu faz muito tempo, mas a sua
descoberta ainda nos beneficia hoje. Louis Pasteur descobriu a cura para muitas
doenças e também já morreu faz muito tempo, mas as suas descobertas médicas
ainda salvam vidas. Leonardo da Vinci, que criou obras de arte, também está
morto, mas aquilo que ele criou ainda é capaz de causar elação e alegria.
Homens nobres e heróis podem ter morrido faz séculos mas ainda podemos obter
inspiração ao ler sobre as suas realizações. De fato, o Buda está morto, mas
2.500 anos depois, os seus ensinamentos ainda ajudam as pessoas, o seu exemplo
ainda inspira as pessoas, as suas palavras ainda mudam vidas. Apenas um Buda é
capaz de ter tal poder séculos depois da sua morte. </span></p>



<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:
PT-BR'>6. O Buda era um Deus?</span></b><span lang=PT-BR style='font-family:
Arial;mso-ansi-language:PT-BR'></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Não, o Buda não era um Deus. Ele nunca fez esse tipo de afirmação, nem
de que ele era o filho de algum Deus ou mesmo o mensageiro de algum Deus. Ele
era um ser humano comum que aperfeiçoou a si mesmo e ensinou que se seguirmos o
seu exemplo também poderemos nos aperfeiçoar. </span></p>



<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:
PT-BR'>7. Se o Buda não é um Deus, então porque as pessoas o veneram?</span></b><span
lang=PT-BR style='mso-ansi-language:PT-BR'></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Há diferentes tipos de veneração. Quando as pessoas veneram um Deus, elas
o honram e glorificam, fazem oferendas e pedem favores, acreditando que o Deus
irá ouvir os seus louvores, receber as oferendas e atender as orações. </span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>O outro tipo de veneração é quando demonstramos respeito por alguém ou
alguma coisa que admiramos. Quando um professor entra na sala de aula ficamos
em pé, quando somos apresentados a algum dignitário o cumprimentamos, quando
ouvimos o hino nacional assumimos um postura respeitosa. Esses são todos gestos
de respeito e veneração e indicam a nossa admiração por pessoas ou coisas. Esse
é o tipo de veneração praticado pelos Budistas. Uma estátua do Buda com as mãos
gentilmente repousadas sobre o colo e com um sorriso compassivo nos lábios nos
recorda do esforço para desenvolver a paz e o amor dentro de nós mesmos. O
perfume do incenso nos recorda da penetrante influência da virtude, as velas
nos recordam da luz do conhecimento e as flores que, em breve irão murchar e
morrer, nos recordam da impermanência. Quando nos curvamos expressamos nossa
gratidão ao Buda por tudo aquilo que nos foi proporcionado pelos seus
ensinamentos. Essa é a natureza da veneração Budista. </span></p>


<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:
PT-BR'>8. Mas eu ouvi as pessoas dizerem que os Budistas veneram ídolos.</span></b><span
lang=PT-BR style='mso-ansi-language:PT-BR'></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Esse tipo de afirmação é um mal-entendido. A definição do dicionário
para a palavra ídolo é “estátua ou simples objeto cultuado como deus ou deusa”.
Como vimos, os Budistas não acreditam que o Buda era um Deus, então como
poderiam acreditar que um pedaço de madeira ou pedra possa ser um Deus? Todas
as religiões empregam símbolos para expressar vários conceitos. No Taoísmo, o
ying-yang é usado para simbolizar a harmonia entre os opostos. No Sikhismo, a
espada é usada para simbolizar a busca espiritual. No Cristianismo, o peixe é
usado para simbolizar a presença de Cristo e a cruz é usada para simbolizar o
seu sacrifício. No Budismo, a estátua do Buda é usada para simbolizar a
perfeição humana. A estátua do Buda também serve para nos recordar da dimensão
humana dos ensinamentos do Buda, e que precisamos olhar para o nosso íntimo e
não para o exterior para encontrarmos a perfeição e a sabedoria. Dizer que os
Budistas veneram ídolos é incorreto. </span></p>



<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:
PT-BR'>9. Porque as pessoas fazem todo o tipo de coisas estranhas nos templos
Budistas?</span></b><span lang=PT-BR style='mso-ansi-language:
PT-BR'></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Muitas coisas podem parecer estranhas quando não as compreendemos. Ao
invés de descartar essas coisas como esquisitas, deveríamos tentar entender o
seu significado. No entanto, é verdade que algumas das práticas Budistas têm as
suas origens nas superstições e mal-entendidos populares ao invés dos
ensinamentos do Buda. E esse tipo de mal-entendido não é encontrado apenas no
Budismo, mas aparece em todas as religiões de tempos em tempos. O Buda ensinou
de forma clara e detalhada e se alguém é incapaz de compreender aquilo que foi
ensinado, o Buda não pode ser censurado por isso. Há um dito:</span></p>



<p class=Normal><i><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Se alguém enfermo não busca tratamento mesmo na presença de um médico,
isso não é culpa do médico.</span></i></p>

<p class=Normal><i><span lang=PT-BR style='font-weight:
normal'>Da mesma forma, se alguém está oprimido e atormentado pela enfermidade
das contaminações mentais, mas não busca a ajuda do Buda, isso não é culpa do
Buda.</span></i></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Tampouco deve o Budismo, ou qualquer religião, ser julgado por aqueles
que não o praticam da forma apropriada. Se alguém quiser conhecer os
verdadeiros ensinamentos Budistas, deve ler as palavras do Buda ou buscar os
ensinamentos através daqueles que os entendem da forma correta. </span></p>



<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:
PT-BR'>10. Porque há tantos tipos distintos de Budismo?</span></b><span
lang=PT-BR style='mso-ansi-language:PT-BR'></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Há muitos diferentes tipos de açúcar: mascavo, branco, granulado, em
pedra, xarope e cristalizado, mas todos são açúcar e todos têm o mesmo sabor
doce. Diferentes formas são produzidas para atender as diferentes necessidades
de uso. Com o Budismo ocorre o mesmo. Há o Theravada, Zen, Terra Pura e
Vajrayana, mas todos são Budismo e todos têm o mesmo sabor – o sabor da liberdade.
O Budismo evoluiu para formatos distintos para ser relevante para as distintas
culturas nas quais ele se estabeleceu. O Budismo foi re-interpretado ao longo
dos séculos para continuar relevante para as novas gerações. Externamente, os
distintos tipos de Budismo podem parecer muito diferentes, mas no núcleo de
cada um encontram-se as quatro nobres verdades e o caminho óctuplo. </span></p>



<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:
PT-BR'>11. O Budismo tem base na ciência?</span></b><span lang=PT-BR
style='mso-ansi-language:PT-BR'></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Antes de responder a essa questão seria melhor primeiro definir a palavra
“ciência”. De acordo com o dicionário, ciência é o “conjunto de conhecimentos
socialmente adquiridos ou produzidos, historicamente acumulados, dotados de
universalidade e objetividade que permitem sua transmissão, e estruturados com
métodos, teorias e linguagens próprias, que visam compreender e possibilitam
orientar a natureza e as atividades humanas.”</span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Há alguns ensinamentos Budistas que não se encaixam nessa definição mas
um dos ensinamentos mais importantes no Budismo, as quatro nobres verdades, com
certeza se encaixa. O sofrimento, que é a primeira nobre verdade, é uma
experiência universal e objetiva que pode ser definida, experimentada e medida.
A segunda nobre verdade afirma que o sofrimento tem uma causa natural, que é o
desejo pelo prazer dos sentidos, que de modo semelhante é uma experiência
universal e objetiva que pode ser definida, experimentada e medida. O Budismo
não tenta explicar o sofrimento com conceitos ou mitos metafísicos. O
sofrimento tem fim de acordo com a terceira nobre verdade e não com a ajuda de
um ser supremo, através da fé ou de orações, mas simplesmente removendo a sua
causa: isso é axiomático. A quarta nobre verdade, o caminho para o fim do
sofrimento, uma vez mais, não tem nada que ver com a metafísica mas depende da adoção
de certos tipos de comportamento. E uma vez mais, o comportamento é algo que
está sujeito a ser testado. O Budismo, tal qual a ciência, prescinde da idéia
de um ser supremo e explica as origens e movimentos do universo com base em
leis da natureza. Tudo isso com certeza exibe um espírito científico. Por outro
lado, o conselho do Buda de que não devemos aceitar as coisas cegamente, mas ao
invés disso, questionar, examinar e investigar tomando por base a nossa própria
experiência pessoal, tem também uma forte conotação científica. O Buda disse:</span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Agora Kalamas, não se deixem levar por relatos, por lendas, pelas
tradições, pelas escrituras, pela conjectura lógica, pela inferência, por
analogia, pela concordância obtida através de ponderações, por probabilidades
ou pelo pensamento, ‘Este contemplativo é o nosso mestre.’<span
style='mso-spacerun:yes'>  </span>Quando vocês sabem por vocês mesmos que,
‘Essas qualidades são hábeis; essas qualidades são isentas de culpa; essas
qualidades são elogiadas pelos sábios; essas qualidades quando postas em
prática conduzem ao bem-estar e à felicidade” - então vocês devem penetrar e
permanecer nelas.” (AN III.65)</span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>Podemos então dizer que embora o Budismo não seja completamente
científico, com certeza ele tem uma forte conotação científica e certamente é a
mais científica das religiões. É significativo que Albert Einstein, um dos
maiores cientistas do século XX, tenha dito sobre o Budismo: </span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“A religião do futuro será uma religião cósmica. Deve transcender um
Deus pessoal e evitar os dogmas e as teologias. Abrangendo ambos, o natural e o
espiritual, ela deve estar baseada num senso religioso que surja da experiência
de todas as coisas, naturais e espirituais, e uma unidade que tenha
significância. O Budismo preenche essa descrição. Se houver alguma religião que
esteja à altura das necessidades científicas modernas, essa religião é o
Budismo.”</span></p>



