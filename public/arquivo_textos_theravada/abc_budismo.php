<p class=Tit2><a name=meditacao></a><b>
Porque devo meditar?</b> <a
href="#Top"><span style='text-decoration:none;text-underline:none'><img border=0 title="Topo" src="../img/tup.gif"></span></a></p>
<a name=Top></a>
