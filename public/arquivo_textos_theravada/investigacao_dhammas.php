<p class=Normal><b>Os Sete Fatores<i><o:p></o:p></i></span></b></p>

<p class=Normal>A palavra em Pali <i>bojjhanga</i>
em geral é traduzida como “fatores da iluminação” e eu também tenho usado essa
tradução por força do hábito. Mas eu realmente prefiro o termo “despertar” ao
invés de “iluminação.” A palavra em Pali possui duas partes: a primeira parte “<i>bojjh</i>”
provém da palavra “<i>bodhi</i>,” baseada na raiz “<i>bodh</i>” que significa
“acordar.” Em termos literais <i>Bodhi </i>significa “despertar,” e “Buda” é
portanto aquele que “é desperto.”<span style="mso-spacerun: yes">  </span>Eu
tento evitar a palavra “iluminação” devido às suas conexões culturais com a
história ocidental. O Iluminismo foi uma iniciativa bastante racionalista e
creio que nos círculos Budistas acabamos ficando atados à palavra “iluminação”
em parte porque os primeiros tradutores ocidentais eram acadêmicos do tipo
racionalista que interpretavam a literatura Budista sob essa perspectiva. Os
primeiros estudiosos não deram muita atenção ao Budismo como um sistema de
meditação.<o:p></o:p></span></p>

<p class=Normal>A segunda parte da
palavra<span style="mso-spacerun: yes">  </span><i>bojjhanga</i> é “<i>anga</i>,”
que em termos literais quer dizer “membro.” Esta palavra é empregada sob várias
formas nos discursos (<i>suttas</i>) em Pali e freqüentemente, como neste caso,
com o sentido de fator ou como componente de algo. Quando o número sete (<i>satta</i>)
é adicionado como prefixo de <i>bojjhanga</i>, ou para a designação mais longa
“<i>sambojjhanga</i>,” obtemos a frase “sete fatores do despertar” ou “sete
componentes do despertar.” A idéia é que os sete componentes não são coisas
separadas – eles prosseguem juntos como um conjunto unitário.<o:p></o:p></span></p>

<p class=Normal>Gostaria de iniciar
explicando como os sete fatores interagem. Nessa explicação, tomarei por base
o<span style="mso-spacerun: yes">  </span><a href="../sutta/MN118.php">Anapanasati
Sutta (Majjhima Nikaya 118)</a>, visto que esses fatores do despertar não são
um tema puramente teórico; eles têm a ver com a nossa prática e em particular
com a prática baseada na respiração delineada no sutta mencionado. Quando
analisamos de perto esses fatores dentro da<span style="mso-spacerun: yes"> 
</span>nossa experiência, podemos ver com clareza como cada um suporta e de
forma natural promove o surgimento do seguinte. Quando um meditador está em
contemplação com a mente bem estabelecida, nesse momento todos os sete fatores
ou <i>bojjhangas </i>surgem em conjunto. Eles ocorrem em conjunto no mesmo
momento e não de forma separada, um conduzindo ao outro em seqüência.<o:p></o:p></span></p>

<p class=Normal>A abordagem
que eu adoto, ao empregar esses sete fatores do despertar como um caminho, é a
de aprender como usá-los na vida diária. De acordo com o meu entendimento do
Dhamma – e eu espero que vocês compartam desse mesmo entendimento – trabalhar
com esses fatores apenas como parte do contexto da meditação formal sentada
significa limitar-nos, limitar o dhamma e limitar a meditação. E isso não é
para depreciar a prática formal; de fato a nossa prática sentada, andando, bem
como a nossa prática nas refeições e no estudo desempenham um papel especial e
importante; mas a minha esperança é que aprendamos como mover-nos sem
dificuldades entre as práticas formais e o dia a dia da vida. <o:p></o:p></span></p>

<p class=Normal><b>Atenção Plena</span></b><span
lang=PT-BR style='mso-ansi-language:PT-BR'><o:p></o:p></span></p>

<p class=Normal>O primeiro dos bojjhangas é
sati, que em geral é traduzido como “atenção plena”. Mas alguns autores, como
por exemplo Thich Nath Hahn, traduzem como “consciência” ou “plena
consciência.” A palavra em Tailandês para sati é “reluk,” que significa
recordar, trazer de volta. Com base na etimologia, a raiz de sati é a mesma
palavra para memória e em alguns contextos sati pode simplesmente significar
memória. Mas com o sentido de atenção plena, sati significa estar sempre
voltando para o presente, enquanto que a memória diz mais respeito ao passado.
Atenção plena também significa confrontar tudo aquilo que surge na nossa
experiência – chegar o mais próximo possível e realmente observar aquilo que
está acontecendo.<o:p></o:p></span></p>

<p class=Normal>Ao praticarmos dessa forma,
com atenção plena e plena consciência, (ou clara compreensão - <i>sampajañña</i>),
sem luta para remover ou agarrar o que quer que seja, ou abandonar os gostos e
desgostos em relação ao mundo – essa é a verdadeira essência da prática. Ao
praticarmos os quatro fundamentos da atenção plena de forma correta – e deve
ser enfatizado que a prática tem que ser correta – então todos os dhammas do
Budismo estarão presentes e assim será fácil para os fatores do despertar
surgirem e persistirem. Quando a atenção plena e a plena consciência são
perfeitas, elas são um antídoto para a interação dos gostos e desgostos, cobiça
e aversão em relação ao mundo. <o:p></o:p></span></p>

<p class=Normal>Quando a atenção plena está
presente dessa forma, essa é a atenção plena fator do despertar. Não se trata
de dois tópicos distintos. Não precisamos praticar algo distinto para que a
atenção plena seja um fator do despertar. Na verdade é bem simples. Tudo que
precisamos fazer é observar a respiração longa como respiração longa e a
respiração curta como curta. A prática desse nível básico é o início da atenção
plena do despertar.<o:p></o:p></span></p>

<p class=Normal>Quando somos capazes de
observar a respiração longa ou curta com atenção plena suficiente, estamos
conscientes ou atentos à respiração longa ao longo de toda a duração da
inalação ou exalação. Caso contrário, a respiração nos escapará, o que
significa dizer que a atenção plena será interrompida e a mente estará pensando
em vários outros assuntos. <o:p></o:p></span></p>

<p class=Normal>Mas assim que chegarmos a
esse nível de prática básico, mesmo na primeira lição dos dezesseis passos
descritos no <a href="../sutta/MN118.php">Anapanasati Sutta</a>, já teremos
iniciado o desenvolvimento da atenção plena como um fator do despertar. <o:p></o:p></span></p>

<p class=Normal>
<b>Investigação dos Dhammas</b><o:p></o:p></span></p>

<p class=Normal>Quando a atenção plena
estiver dessa forma engajada, então teremos a capacidade ou a oportunidade para
o segundo fator do despertar: investigação dos dhammas ou <i>dhamma-vicaya</i>.
<i>Vicaya</i> significa a investigação, o escrutínio ou análise. Podemos
interpretar isso como a análise intelectual, ou podemos considerá-lo num
contexto meditativo como o escrutínio não intelectual. Dependendo da forma como
interpretamos essa análise, a palavra dhamma pode significar algo tão preciso
quanto um estado mental em particular, ou pode ter um significado mais amplo
abrangendo tudo aquilo de importância nas nossas vidas.<o:p></o:p></span></p>

<p class=Normal>Quando a atenção plena
estiver bem estabelecida, então essa atenção plena poderá tomar uma parte da
experiência e investigá-la – selecioná-la, mantê-la e submetê-la ao escrutínio.
Gosto de pensar na imagem de um joalheiro trabalhando com precisão. Você pode
ver esses artífices com os seus pequenos monóculos através dos quais eles
observam uma jóia e investigam-na com extremo cuidado. Eles examinam a sua cor,
textura, brilho e formato. Eles examinam-na para identificar se possui algum
tipo de defeito. E eles não a mantêm numa única posição; eles giram-na para ver
como ela é sob a luz por diferentes ângulos, e assim por diante. Esse processo
de investigação é bastante ativo; há participação; é dinâmico; é engajado. Como
é descrito no <a href="../sutta/MN118.php">Anapanasati Sutta</a>, o meditador
“investiga e examina esse estado com sabedoria e faz uma indagação completa a
respeito disso.” Essa é a qualidade de investigação que emerge naturalmente da
atenção plena como o segundo fator do despertar.<o:p></o:p></span></p>

<p class=Normal>Na atenção plena na
respiração, o que importa é que a mente esteja atenta – esteja totalmente
consciente a cada inspiração e expiração. Já a investigação é um pouco distinta
da mera notação. Na prática da notação podemos observar “ali está um
pensamento, agora uma sensação” e assim por diante. Existe um certo grau de
atenção plena nisto, mas lhe falta a qualidade de investigação que é a
característica do segundo fator do despertar.<o:p></o:p></span></p>

<p class=Normal>É importante observar
aquilo que ocorre na mente, mas também é de grande ajuda examinar o efeito que
esses coisas causam. A maioria dos mestres explica a atenção plena de uma forma
que inclui algum tipo de investigação. Quer você fale sobre ela como duas
coisas ou uma só, o que importa é que a investigação ocorra. Essa é a prática
consciente. <o:p></o:p></span></p>

<p class=Normal><b>Compreendendo Aquilo que
é Importante</span></b><o:p></o:p></span></p>

<p class=Normal>Ajahn Buddhadasa sempre
enfatizava a compreensão ampla da palavra dhamma no composto <i>dhammavicaya</i>.
Visto que os dhammas são praticamente infinitos, uma pessoa poderia passar toda
a sua vida perseguindo os mais ínfimos e insignificantes detalhes e chamar isso
de prática. Mas Ajahn Buddhadasa enfatizava que existem muitos dhammas com os
quais não é necessário gastar o nosso tempo. Por exemplo, só sentar em
meditação e observar tudo aquilo que surge na mente pode ser generalização em
demasia e também um tanto quanto confuso. É verdade que ter a capacidade de
observar aquilo que surge na mente, observando apenas a sua origem e cessação,
é uma boa prática de atenção plena. Mas no fluxo interminável de coisas que
surgem e desaparecem, existem alguns fenômenos que são problemáticos e que
requerem mais atenção. <o:p></o:p></span></p>

<p class=Normal>O Buda empregou diferentes
abordagens ao longo dos suttas para descrever o que merece mais atenção. Um par
freqüente de termos é “<i>kusala</i>” e “<i>akusala</i>” – benéfico/saudável e
prejudicial/insalubre; hábil e inábil. <i>Kusa</i> é um tipo de capim com as
bordas afiadas e também significa cortar. <i>Kusala</i> significa cortar fora
os desejos, apegos, as identificações do ego, as contaminações que nos mantêm
dando voltas no samsara. <i>Akusala</i> é não cortar, ou cortar as coisas
erradas. <i>Kusala</i> é desenvolver aquilo que é saudável tal como a
tolerância, <i>metta</i>/amor bondade, <i>karuna</i>/compaixão e todas as
demais qualidades benéficas e hábeis, <i>akusala</i> é o oposto de tudo isso.<o:p></o:p></span></p>

<p class=Normal>Assim, ao investigar você
pode usar essa idéia simples de observar aquilo que surge: é benéfico ou
prejudicial? É algo que nos mantém aprisionados ao sofrimento, ou é algo que
leva à libertação do sofrimento? É algo que nos auxilia a soltar-nos das nossas
tendências egoístas e assim por diante? Quando investigamos os dhammas, investigamos
principalmente aqueles dhammas que ainda são problemáticos nas nossas vidas,
tal como o medo, dúvida, ansiedade, ressentimento, orgulho, todas aquelas
coisas que nos mantêm aprisionados. Ou investigamos os dhammas benéficos, as
qualidades espirituais saudáveis, as emoções saudáveis ou os estados mentais
saudáveis de modo a fortificá-los, para possibilitar que eles nos despertem.<o:p></o:p></span></p>

<p class=Normal>Ter habilidade na prática -
empregar <i>upaya</i>, ou meios hábeis – significa concentrar-se naquelas
qualidades que são importantes nas nossas vidas. Com freqüência são coisas
conectadas com o sofrimento, com <i>dukkha</i>. A prática com habilidade
envolve a investigação de coisas como os nossos apegos favoritos, porque estes
criam sofrimento diretamente para nós e para os outros. Envolve a investigação
das nossas opiniões, emoções, desejos, identidades, mecanismos de defesa –
essas são as coisas relacionadas diretamente com o sofrimento e são as mais
importantes nessa investigação. Se, por exemplo, não existir muita abertura ou
flexibilidade nas nossas vidas, ou se não tivermos muita habilidade de perdoar,
ou para sermos generosos, tolerantes, pacientes – se qualquer uma dessas
qualidades ou dhammas benéficos for fraca ou estiver ausente ou for
inconsistente – então isso é algo a ser investigado. <o:p></o:p></span></p>

<p class=Normal><i>Pergunta</span></i><span
lang=PT-BR style='mso-ansi-language:PT-BR'>: Esse tipo de investigação que você
está descrevendo envolve um certo nível de julgamento intelectual e isso não
nos afastará da nossa experiência levando-nos na direção do raciocínio
conceitual?<o:p></o:p></span></p>

<p class=Normal>Pode ser um processo
intelectual, no qual empregamos a mente racional e analisamos as coisas
vendo-as por ângulos distintos. E a pessoa tem que ser cuidadosa para não se
deixar aprisionar por uma determinada posição intelectual ou racional qualquer.
Mas no sentido mais amplo da palavra, apesar de haver espaço para a
investigação intelectual, há também a implicação de uma forma de investigação
mais sutil, que eu creio ser essencial na meditação. E nesse caso não usamos
tanto o intelecto. <o:p></o:p></span></p>

<p class=Normal>Eu sinto que o intelecto é
muito bom para levantar questões, para colocá-las no seu devido contexto e para
dirigir a atenção para as coisas. Mas a real investigação das coisas, creio,
vai além do intelecto. Freqüentemente, para compreendermos o não verbalizado
precisamos empregar o intelecto, portanto num estágio inicial o pensamento pode
desempenhar um papel de ajuda. O pensamento dirige a nossa atenção para aquilo
que está realmente acontecendo, mas depois a parte conceitual perde a sua
importância. Mas muito depende da experiência e dos meios hábeis – você aprende
com a sua própria mente e aprende a trabalhar com aquilo que funciona. Existem
ocasiões em que o pensamento lúcido é necessário, e algumas vezes não pensar é
até mesmo irresponsável.<o:p></o:p></span></p>

<p class=Normal>Ajahn Buddhadasa disse-me
numa conversa, certa vez, que a sua prática era guiada pelo intelecto. Ele era
considerado um intelectual e um pensador, mas ao mesmo tempo ele era dedicado à
prática. Muito do seu trabalho intelectual esteve direcionado para identificar
aquilo que era importante e no que colocar a atenção.<span style="mso-spacerun:
yes">  </span>E uma vez que você tenha usado o seu intelecto para guiar sua
atenção, então a prática será a investigação num nível mais profundo, a
contemplação das coisas com cuidado, o exame em profundidade e assim por
diante. <o:p></o:p></span></p>

<p class=Normal>A investigação do conteúdo
benéfico ou prejudicial daquilo que estiver ocorrendo na mente pode ser num
plano verbal, como uma reflexão; mas uma vez que a mente esteja bem treinada
podemos abandonar as palavras e o processo se converte num escrutínio mais
íntimo, não verbal. Como forma de aprendizado, podemos examinar a natureza da
nossa experiência de tempos em tempos até que nos acostumemos a observar as
coisas dessa forma. Mais tarde isso se tornará um hábito. No caso do joalheiro
mencionado acima, eu não creio que ele tenha que “pensar” naquilo que está fazendo
ao examinar uma jóia. O seu treinamento já se tornou uma parte da forma como
ele vê as coisas e assim a investigação e a observação não são duas coisas
distintas.<o:p></o:p></span></p>

<p class=Normal>Algumas vezes as pessoas
usam o termo “atenção sem preferências,” e ao longo dos anos tenho ouvido essa
expressão ser empregada de diversas formas – algumas das quais bem confusas. Se
a atenção for realmente sem preferências, não haverá reação. E na verdade muito
poucas pessoas são capazes de praticar a verdadeira atenção sem preferências: quando
todos os sete fatores estão bem desenvolvidos. Se os estados benéficos e
prejudiciais continuarem a surgir, não seremos capazes de permanecer totalmente
sem preferências. É fácil subestimar o valor das coisas; as coisas com as quais
nos sentimos confortáveis, as coisas que gostamos e não gostamos de olhar.
Todos nós tendemos a acumular muitos hábitos e depois não queremos ver isso.
Mas com freqüência quando existe um hábito formado existe uma grande atividade
do ego e é exatamente para isso que devemos olhar. <o:p></o:p></span></p>

<p class=Normal>Para isso, usamos quaisquer
ferramentas que nos auxiliem a ver as experiências de uma forma que revelem os
seus aspectos ocultos. Pode ser algo muito simples como: isso é sofrimento ou
não sofrimento? É benéfico ou prejudicial? Esse tipo de questão não é um<span
style="mso-spacerun: yes">  </span>julgamento. Os suttas na verdade não
proporcionam uma relação detalhada daquilo que é prejudicial, mas nos encorajam
a olhar para a experiência e ver como ela é prejudicial, porque se encontra
ali, de onde vem. Esse é o processo de investigação. <o:p></o:p></span></p>

<p class=Normal><b>Investigando o Consenso</span></b></p>
<p class=Normal>Como esse processo de investigação funciona na vida diária? Charles Tart, um
conhecido pesquisador e escritor sobre a consciência, emprega o termo “transe
consensual.” Eu considero este termo muito útil porque expressa o mundo no qual
gastamos muito do nosso tempo. É um transe porque de certo modo nós estamos
perdidos nele sem realmente saber o que estamos fazendo, o que somos, o que
está acontecendo. E é um consenso porque nós o criamos com os outros por meio
da linguagem, através da cultura compartida, através da educação e da mídia.
Nós criamos um pacote completo com crenças, premissas, expectativas, valores,
teorias, dogmas, “ismos,” que em geral são presumidos mas não muito examinados.<o:p></o:p></span></p>

<p class=Normal>A despeito disso, certos
aspectos dessa construção cultural da realidade podem ser examinados através da
investigação. Eu tenho alguns amigos que são Marxistas e eles empregam a
análise Marxista para avaliar o aspecto das classes sociais no transe
consensual e talvez, pelo menos em termos de classes sociais, ele comecem a
sair um pouco do transe. Ou as feministas irão encarar isso sob a perspectiva
de gênero e revelar as formas camufladas através das quais os nossos
pensamentos são moldados pelo consenso. Os psicólogos irão olhar para o transe em
relação a certas teorias psicológicas, e assim por diante. <o:p></o:p></span></p>

<p class=Normal>O meu entendimento é que a
investigação dos dhammas implica em começar a avaliar as premissas, crenças,
teorias, dogmas, ideologias que até certo ponto trazemos dentro de nós talvez
até inconscientemente. É possível meditar bastante, especialmente em retiros
longos, nos quais a pessoa pode ter experiências que são vívidas, claras e
profundas quando ocorrem. Mas em pouco tempo somos arrastados de volta para as
antigas estruturas intelectuais nas quais amadurecemos – aquilo que aprendemos
na escola, nosso treinamento numa universidade ou numa profissão. Pode ser uma
ideologia política, um preconceito cultural, uma excentricidade na
personalidade ou qualquer outra coisa que tenha criado a estrutura de opiniões,
crenças e idéias nas quais vivemos. Creio ser importante gastar parte do nosso
tempo investigando essas premissas, crenças e preconceitos culturais e permitir
que a nossa prática de meditação nos liberte desse tipo de coisas – questões de
racismo, patriarcado, sexismo, só para nomear algumas.<o:p></o:p></span></p>

<p class=Normal>Eu tive a boa sorte, creio,
nesse aspecto, porque cresci num país, e na época em que estava me tornando um
adulto, (se isso já aconteceu mesmo, tenho minhas dúvidas), fui para uma
cultura muito diferente na qual algumas das crenças e premissas eram diferentes
daquelas com as quais eu havia crescido. Assim passei uma boa parte da minha
vida com uma certa dissonância entre as minhas premissas e aquilo que estava
ocorrendo à minha volta. E então, depois de viver quase 20 anos na Tailândia,
regressando aos Estados Unidos de tempos em tempos, essa dissonância continua,
o que de certa forma é divertido. Mantém-me em estado de alerta. <o:p></o:p></span></p>

<p class=Normal><b>A Natureza das Idéias</span></b><span
lang=PT-BR style='mso-ansi-language:PT-BR'><o:p></o:p></span></p>

<p class=Normal>Alguns dos suttas em Pali
da época do Buda nos dão exemplos do tipo de idéias fixas que os filósofos e
religiosos estavam formulando. Ao mesmo tempo que a terminologia dessas idéias
pode parecer estranha aos nossos ouvidos modernos, o significado ainda é
contemporâneo e é possível que nós também tenhamos idéias fixas com relação a
assuntos que foram discutidos na época do Buda: “O universo é infinito ou
finito?” Esse é um debate atual na cosmologia, sob a forma de se o universo
continuará a se expandir indefinidamente ou em algum momento irá se contrair.
Ou: “A alma e o corpo são a mesma coisa ou são diferentes?” Esse é um debate em
andamento na ciência cognitiva, tendo a ver com a natureza da consciência e a
relação entre a mente e o corpo.<o:p></o:p></span></p>

<p class=Normal>Mas se pudermos ver que
essas são apenas construções mentais, que se encontram fora da nossa própria
experiência, que nós as criamos coletivamente, então não precisamos gerar apego
por elas. As idéias são fixas quando você crê que o conteúdo da idéia é algo
duradouro, permanente e verdadeiro. Mas se você vir isso apenas como uma idéia
que foi criada na mente de uma pessoa, ou de um grupo, que foi passada de
geração para geração, então é difícil se apegar de um modo fixo. Quantas
opiniões temos às quais ainda estamos apegados porque ainda não vimos que são
opiniões criadas? Esse é um assunto para ser investigado.<o:p></o:p></span></p>

<p class=Normal>O Buda falava sobre o
“entendimento correto” no sentido de ver a verdade de algo sem se apegar a
isso. Só com a observação e investigação cuidadosas podemos ver que os dhammas
são impermanentes, insatisfatórios e vazios. Mas esse tipo de entendimento
correto é bem distinto da atitude de quem cria um sistema filosófico baseado
numa idéia e depois sente-se superior em relação às demais pessoas que não
compartem da mesma perspectiva. Alguns Budistas são conhecidos pelo apego à
noção do desapego e aí usam isso para criticar os outros. Ou você poderá ouvir
que para ser um Budista terá que ter certas idéias. Um autor mencionou
recentemente que se você não acreditar no renascimento não poderá ser
considerado um Budista.<o:p></o:p></span></p>

<p class=Normal>Ao olharmos para a tarefa
de investigar as idéias sob a perspectiva da prática contida no <a
href="../sutta/MN118.php">Anapanasati Sutta</a>, veremos que a ênfase está na
impermanência e no desapego. O ponto é que, onde quer que coloquemos a atenção
plena, quer sejam nos elementos do caminho, nas cinco faculdades, nos cinco
poderes – ou em todos os demais itens relacionados nesse sutta – deveríamos
estar vendo a impermanência daquilo que estiver sendo experimentado, a sua
instabilidade, o fato de que o que quer que esteja surgindo depende de outras
coisas. E que, portanto, não possui uma existência inerente independente. Só o
que existe é a interdependência, a ausência de uma essência, o vazio em tudo.<o:p></o:p></span></p>

<p class=Normal><b>Lavrar os seus Campos
com os Fatores do Despertar</span></b><span lang=PT-BR style='mso-ansi-language:
PT-BR'><o:p></o:p></span></p>

<p class=Normal>Muitas das lições de Ajahn
Buddhadasa envolvem a aplicação dos ensinamentos na vida diária. A sua tese era
que o objetivo de ser um Budista é viver sem sofrimento e não só em alguma
caverna ou num retiro de meditação, ou num monastério, mas no meio de qualquer
coisa que estejamos fazendo. E a forma de viver sem sofrimento é transformar
qualquer coisa que estejamos fazendo – quer seja lavrando os campos de arroz,
escovando os dentes, lavando a louça – numa prática do caminho. Ele tinha suas
raízes no meio ambiente em que vivia e por isso com freqüência falava sobre os
sete fatores do despertar em termos coloquiais.<o:p></o:p></span></p>

<p class=Normal>Na época em que ele viveu,
os agricultores tailandeses da vizinhança do monastério de Suan Mokh ainda
usavam búfalos para lavrar os seus campos. Na atualidade isso quase não existe
mais devido às mudanças econômicas e quase não há mais plantio de arroz naquela
região, mas no passado não era assim. Ele costumava dizer aos aldeãos: “Para
que um agricultor lavre os seus campos, ele precisa usar os sete fatores do despertar.”<o:p></o:p></span></p>

<p class=Normal>Primeiro, você tem que
estar muito atento àquilo que estiver fazendo. Você tem que estar atento ao
búfalo e você tem que estar atento ao arado. Se o arado afundar em demasia irá
ficar entalado; se ficar muito raso, não irá produzir nenhum resultado. Você
tem que estar atento no ponto em que irá virar, aos sinais que estiver dando ao
búfalo e uma quantidade de outros fatores.<o:p></o:p></span></p>

<p class=Normal>E não só atento, mas você
precisa estar constantemente investigando enquanto estiver lavrando o campo com
o búfalo. A mente tem que estar alerta verificando as coisas à medida que você
avança: examinando a qualidade do solo, o grau de umidade, o estado do búfalo,
a localização dos obstáculos. Você tem que ir aprendendo com o processo.<o:p></o:p></span></p>

<p class=Normal>Você terá que fazer esforço
– tanto físico como mental. Sem esforço você poderá se distrair e ficar
relaxado. Se o seu búfalo for bom ele poderá seguir adiante por algum tempo;
mas alguns búfalos são maliciosos como cavalos e mulas, e se eles perceberem
alguma negligência no seu controle, tirarão proveito disso.<o:p></o:p></span></p>

<p class=Normal>Você também precisa ter um
certo grau de satisfação naquilo que estiver fazendo. Quer você esteja lavrando
um campo ou praticando meditação, qualquer coisa pode ser tediosa ou fatigante
se você não obtiver satisfação disso. Ou poderá ser divertido num sentido
dhammico – interessante, recompensador. Somos mais capazes de dedicar-nos a
algo que consideramos satisfatório sob uma perspectiva saudável.<o:p></o:p></span></p>

<p class=Normal>E essa satisfação trará um
estado de calma. Se estivermos lavrando o nosso campo, mas estivermos
fisicamente agitados, iremos irritar ou assustar o búfalo. Se a nossa mente
estiver agitada, irá interferir com alguns dos outros fatores. É mais fácil
estar presente, quer seja lavrando o campo ou fazendo meditação, quando estamos
calmos. <o:p></o:p></span></p>

<p class=Normal>Quando a mente se acalma,
ela naturalmente fica mais focada. As forças que agitam e distraem a mente são
eliminadas e é mais fácil para a mente ficar estabelecida sobre uma única
tarefa. Tanto o agricultor como o búfalo precisam permanecer sem distrações se
o campo tiver que ser arado antes do anoitecer.<o:p></o:p></span></p>

<p class=Normal>Quando todos esses fatores
estão presentes –<span style="mso-spacerun: yes">  </span>se reforçando
mutuamente – a mente ganha a habilidade de vigiar e manter as coisas no seu
caminho. O esforço se torna sem esforço, por assim dizer. A equanimidade é assim
– quando você está completamente equilibrado mas ainda possui algum momentum.
Você não comete erros e parece que a tarefa progride por conta própria.<o:p></o:p></span></p>

<p class=Normal>Assim é como os sete <i>bojjhangas</i>
podem atuar em conjunto para converter qualquer atividade diária numa prática
recompensadora, progredindo no caminho do despertar.<o:p></o:p></span></p>


