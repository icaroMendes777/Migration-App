<p class=Normal>O Budismo toma um princípio americano conhecido – a busca da
felicidade – e insere dois importantes qualificativos. A felicidade objetivada
é <i>verdadeira: </i>última, imutável e não enganosa. A busca dessa felicidade
é <i>séria,</i> não no sentido amargo, mas dedicada, disciplinada e disposta a
fazer sacrifícios inteligentes.</p>


<p class=Normal>Que tipo de sacrifícios são inteligentes? A resposta Budista
a essa questão ressoa um outro princípio americano: um sacrifício inteligente é
aquele em que você obtém uma felicidade maior ao abrir mão de uma felicidade
menor, da mesma forma que você abriria mão de um saco cheio de doces se fosse
oferecido um quilo de ouro em troca. Em outras palavras, um sacrifício
inteligente é igual a uma transação lucrativa. Essa analogia é muito antiga na
tradição Budista. “Eu farei uma troca,” um dos discípulos do Buda disse certa
vez, “o envelhecimento por aquilo que não envelhece, a queimação pela
libertação: a paz máxima, a insuperável segurança contra o cativeiro.”</p>

<p class=Normal>Existe algo em todos nós que prefere não abrir mão das
coisas. Preferiríamos ficar com os doces <i>e também </i>obter o ouro. Mas a
maturidade nos ensina que não podemos ter tudo, que gozar um prazer, talvez
melhor, com freqüência envolve abrir mão de outro. Assim, precisamos
estabelecer prioridades claras para investir o nosso tempo e energia limitados
aonde possam produzir os resultados mais duradouros.</p>

<p class=Normal>Isso significa dar máxima prioridade para a mente. As coisas
materiais e relacionamentos sociais são instáveis e facilmente afetados por
forças fora do nosso controle, portanto a felicidade que eles oferecem é fugaz
e pouco confiável. Mas o bem estar de uma mente bem treinada pode sobreviver
até mesmo ao envelhecimento, enfermidade e morte. Para treinar a mente, no
entanto, é necessário tempo e energia. Essa é uma das razões porque a busca da
verdadeira felicidade demanda que sacrifiquemos alguns dos nossos prazeres
externos.</p>


<p class=Normal>Sacrificar prazeres externos também nos liberta do fardo
mental que com freqüência está ligado à manutenção deles. Uma estória famosa no
Cânone descreve um antigo rei que, depois de se tornar um monge, sentou-se ao
pé de uma árvore e exclamou, “Que felicidade! Que felicidade!” Os seus
companheiros monges pensaram que ele estava desejando os prazeres que
desfrutava quando era rei, mas mais tarde ele explicou ao Buda qual felicidade
ele tinha em mente:</p>


<p class=Normal>“Antes, quando eu era um rei, eu tinha guardas postados
dentro e fora dos aposentos, dentro e fora da cidade, dentro e fora da área
rural. Mas mesmo estando guardado dessa forma, protegido dessa forma, eu vivia
com medo – agitado, desconfiado e amedrontado. Porém agora, indo sozinho para a
floresta, para o pé de uma árvore, ou uma cabana vazia, eu permaneço sem medo,
sem agitação, confiante e destemido – despreocupado, calmo, minhas necessidades
satisfeitas, com a minha mente como um gamo selvagem.”</p>


<p class=Normal>Uma terceira razão para sacrificar prazeres externos é que
ao buscar certos prazeres – tais como os nossos vícios dos ‘doces do olho,’
‘doces do ouvido,’ ‘doces do nariz,’ ‘doces da língua,’ ‘doces do corpo’ –
estimulamos qualidades de cobiça, raiva e delusão que com vigor bloqueiam as
qualidades necessárias para a paz interna. Mesmo se tivéssemos todo o tempo e
energia do mundo, a busca desses prazeres nos afastaria cada vez mais do
objetivo. Isto está expresso no elemento do caminho da prática denominado Pensamento
Correto: a determinação pessoal de abrir mão de todo tipo de pensamento que
envolva a cobiça sensual, má vontade e aquilo que é prejudicial. “Cobiça
sensual” compreende não apenas o desejo sensual, mas qualquer desejo pelos
prazeres dos sentidos que perturbem a paz mental. “Má vontade” abrange qualquer
desejo de sofrimento, para si mesmo ou para os outros. E “prejudicial” é
qualquer atividade que concretize esse sofrimento. Dessas três categorias, as
duas últimas são as mais fáceis de ver como dignas de serem abandonadas. Talvez
não seja sempre fácil abandoná-las, mas a determinação de abandoná-las é
obviamente uma coisa boa. A primeira determinação no entanto – renunciar à
cobiça sensual – é até difícil de formular quanto mais de seguir.</p>


<p class=Normal>Parte da nossa resistência a essa determinação é uma
característica humana universal. As pessoas em todos os lugares se deleitam com
as suas paixões. Mesmo o Buda admitiu aos seus discípulos que, ao iniciar o
caminho da prática, o seu coração não saltou sobre a idéia de renunciar à
cobiça sensual, não viu que isso poderia proporcionar paz. Mas um elemento
adicional da nossa resistência à renúncia é peculiar à cultura ocidental. A
moderna psicologia popular ensina que a única alternativa ao saudável gozo das
nossas paixões sensuais é a temida, insalubre, repressão. No entanto, <i>ambas </i>as<i>
</i>alternativas têm por base o temor: repressão, baseada no temor do que a
paixão poderia fazer se fosse expressa ou até mesmo permitida na consciência;
entrega, baseada no temor da privação e do monstro no qual a paixão poderia se
converter se fosse resistida e reprimida. Ambas as alternativas impõem sérias
limitações à mente. O Buda consciente das desvantagens de ambas, teve a idéia
de encontrar uma terceira alternativa: uma abordagem destemida e hábil que
evita os perigos de cada extremo.</p>


<p class=Normal>Para entender a abordagem dele no entanto, temos que ver
como o Pensamento Correto se relaciona com os outros elementos do caminho
Budista, particularmente o Entendimento Correto e a Concentração Correta. Na
análise formal do caminho, o Pensamento Correto se apóia sobre o Entendimento
Correto; a sua manifestação mais hábil<span style="mso-spacerun: yes"> 
</span>é aquela na qual ele funciona como o pensamento aplicado e sustentado
que conduz a mente à Concentração Correta. O Entendimento Correto proporciona o
meio hábil para compreender a cobiça e os prazeres sensuais para que a nossa
abordagem do problema não escape do objetivo; a Concentração Correta
proporciona estabilidade interna e felicidade de forma que possamos com clareza
ver as raízes da cobiça e ao mesmo tempo não temer a perda com a possibilidade
de desenraizá-las.</p>


<p class=Normal>Existem dois níveis de Entendimento Correto, que focam em
(1) resultados das nossas ações no processo das nossas vidas e (2) aspectos do
sofrimento e a sua cessação na mente. O primeiro nível aponta as desvantagens
da cobiça sensual: os prazeres sensuais são fugazes, instáveis e estressantes;
a cobiça por eles está na raiz de muitos males da vida, indo desde as
dificuldades em obter e manter a riqueza, até brigas em família e guerras entre
nações. Esse nível de Entendimento Correto nos prepara para enxergar a entrega
às paixões sensuais como um problema. O segundo nível – vendo as coisas sob a
perspectiva das quatro nobres verdades – nos mostra como resolver esse problema
na nossa abordagem do presente momento. Ele indica que a raiz do problema não
está nos prazeres mas na cobiça, pois a cobiça envolve apego e qualquer apego a
prazeres condicionados conduz inevitavelmente ao estresse e sofrimento, já que
todos os fenômenos condicionados estão sujeitos à mudança. Na verdade, o nosso
apego à cobiça sensual tende a ser mais forte e mais constante do que o nosso
apego a prazeres em particular. Esse apego é o que tem que ser renunciado.</p>

<p class=Normal>Como isso é feito? Trazendo o assunto à baila. Ambos os
aspectos do apego – como padrões habituais do passado e a nossa predisposição
em submeter-nos a eles novamente no presente – estão baseados em <a name=PVW>mal-entendidos</a>
e temor. Como foi apontado pelo Buda, a cobiça sensual depende de percepções
aberrantes: nós projetamos noções de constância, paz, beleza e eu em coisas que
na verdade são inconstantes, estressantes, feias e não-eu. Esses mal-entendidos
se aplicam tanto às nossas paixões como aos seus objetos. Percebemos a
expressão da nossa sensualidade como algo atraente, uma profunda expressão da
nossa auto-identidade que oferece prazer duradouro; vemos os objetos das nossas
paixões como duradouros e sedutores, como estando sob nosso controle o
suficiente, que proporcionam uma satisfação que não irá se converter no seu
oposto. Na verdade, nada disso é correto e no entanto acreditamos cegamente nas
nossas projeções porque o poder dos nossos apegos apaixonados têm-nos muito
intimidados para vê-los com clareza. Assim, os seus efeitos especiais mantêm-nos
fascinados e ludibriados. Enquanto estivermos operando apenas com a entrega e a
repressão, o apego poderá continuar operando livremente na escuridão do
subconsciente. Mas quando resistirmos de forma consciente, o apego terá que vir
para a superfície, articulando as suas ameaças, demandas e racionalizações.
Portanto, embora os prazeres sensuais em si não representem aquilo que é
prejudical, precisamos de forma sistemática abrir mão deles como um meio de
revelação do esquema dos apegos. É dessa forma que a renúncia com habilidade
serve como ferramenta de aprendizado, descobrindo os esquemas latentes que
tanto a entrega como a repressão querem manter abaixo da superfície.</p>


<p class=Normal>Ao mesmo tempo, precisamos prover a mente com estratégias
para resistir a esses esquemas e eliminá-los quando surgirem. É onde entra a
Concentração Correta. Como meio hábil de gozo, a Concentração Correta impregna
o corpo com o êxtase e prazer não sensual que pode ajudar a compensar a
sensação de perda ao resistir às paixões sensuais. Em outras palavras, ela
provê prazeres mais sublimes – mais duradouros e refinados – como recompensa
pelo abandono dos mais inferiores. Ao mesmo tempo ela nos proporciona uma base
estável que necessitamos para não sermos arrastados pelos assaltos dos nossos
apegos frustrados. Essa estabilidade também fortifica a atenção plena e plena
consciência que necessitamos para ver com clareza os mal-entendidos e delusões
que estão por trás da cobiça sensual. E uma vez que a mente possa ver com
clareza os processos de projeção, percepção e<span style="mso-spacerun: yes"> 
</span>erros de percepção, uma sensação de liberdade mais elevada surgirá
quando eles forem superados e a base para a cobiça sensual desaparecerá.</p>


<p class=Normal>Nesse estágio podemos então analisar nosso apego aos
prazeres da Concentração Correta. Quando a nossa compreensão estiver completa,
abandonaremos <i>todas </i>as necessidades de apego de qualquer tipo e assim
encontraremos o ouro puro de uma liberdade tão abrangente que não pode ser
descrita.</p>


<p class=Normal>A questão permanece: como essa estratégia de renúncia e gozo
com habilidade se traduz para a prática diária? As pessoas que se ordenam como
monges assumem votos de celibato e espera-se que eles se empenhem de forma
constante na renúncia à cobiça sensual, mas para muitas pessoas essa não é uma
opção viável. O Buda então recomendava que os seus discípulos leigos
observassem períodos de um dia de renúncia temporária. Quatro dias em cada mês
– tradicionalmente nos dias de lua cheia, nova, quarto minguante e crescente –
eles podem observar os oito preceitos, que adicionam as seguintes regras ao
padrão de cinco preceitos: celibato, não se alimentar após o meio dia, não
assistir a espetáculos, não ouvir música, não usar perfumes e cosméticos e não
usar camas e assentos luxuosos. O propósito desses preceitos adicionais é de
conter de forma razoável os cinco meios dos sentidos. O dia é então dedicado a
ouvir o Dhamma, clarificar o Entendimento Correto, e à pratica de meditação,
fortalecendo a Concentração Correta. Embora a semana de trabalho moderna possa
fazer com que o calendário lunar desses dias de retiro seja impraticável,
existem formas de integrá-los nos finais de semana ou outros dias de descanso
durante a semana. Dessa forma, qualquer pessoa interessada pode, a intervalos
regulares, trocar as preocupações e complexidades do dia a dia pela
oportunidade de praticar a renúncia como uma habilidade essencial na busca
séria pela felicidade no sentido mais verdadeiro da palavra. </p>


<p class=Normal>E essa não seria uma troca inteligente?</p>

