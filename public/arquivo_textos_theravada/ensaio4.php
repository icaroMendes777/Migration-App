<p class=Normal>Uma antiga máxima que é
encontrada no <i>Dhammapada</i> resume a prática dos ensinamentos do Buda em
três simples instruções: evitar todo o mal, cultivar o bem, purificar a própria
mente. Esses três princípios formam uma seqüência graduada de passos iniciando
com aquilo que é preparatório e externo e progredindo até aquilo que é
essencial e interno. Cada passo conduz com naturalidade ao que segue, e a
culminação dos três na purificação da mente torna claro que é aí que se
encontra o núcleo da prática Budista. <o:p></o:p></span></p>

<p class=Normal>A purificação da mente, da
forma como é entendida nos ensinamentos do Buda, é perseverar no esforço para
limpar a mente das impurezas, aquelas forças mentais sombrias e prejudiciais,
que correm por baixo da superfície do fluxo da consciência, viciando o nosso
pensamento, valores, atitudes e ações. As três principais impurezas são aquelas
que o Buda denominou de as &quot;raízes do mal&quot; - cobiça, raiva e delusão
- das quais emergem inúmeras ramificaçõões e variantes: ódio e crueldade,
avareza e inveja, presunção e arrogância, hipocrisia e vaidade, toda a
multiplicidade de entendimentos incorretos. <o:p></o:p></span></p>

<p class=Normal>As atitudes contemporâneas
não encaram com bons olhos as noções de impurezas e purificação e, à primeira
vista, pode parecer um retrocesso que nos leva a um moralismo fora de época,
talvez válido em uma época em que o pudor e o tabu dominavam, mas não para nós
que representamos a vanguarda da modernidade emancipada. Sem dúvida, nem todos
nos deliciamos com o gozo do materialismo grosseiro e muitos dentre nós
buscamos a iluminação e elevação espiritual, só que nós as queremos sob certas
condições e como herdeiros da nova liberdade acreditamos que elas devam ser
obtidas através de uma busca desenfreada por diferentes experiências sem
nenhuma necessidade em particular de introspecção, mudança pessoal ou
autocontrole. <o:p></o:p></span></p>

<p class=Normal>No entanto, nos
ensinamentos do Buda o parâmetro da iluminação genuína se encontra exatamente
na pureza da mente. O propósito de todo o insight e entendimento iluminado é o
de libertar a mente das impurezas e Nibbana, o objetivo do ensinamento, é
definido de forma clara como a libertação da cobiça, raiva e delusão. Sob a
perspectiva do Dhamma as impurezas e a pureza não são meros postulados de um
moralismo autoritário mas fatos reais e concretos, essenciais ao entendimento
correto da situação humana no mundo. <o:p></o:p></span></p>

<p class=Normal>Como fatos da experiência
de vida, as impurezas e a pureza apresentam uma diferença fundamental tendo um
significado crucial para aqueles que buscam libertar-se do sofrimento. Elas
representam os dois pontos entre os quais o caminho da libertação se revela --
o primeiro é problemático e o ponto de partida, o último é a solução e o fim. O
Buda declara que as impurezas se encontram na base de todo o sofrimento humano.
Queimando internamente como luxúria e desejo, como raiva e ressentimento, elas
destroem o coração, vidas, esperanças e civilizações e nos conduzem cegos e
sedentos através dos ciclos de nascimento e morte. O Buda descreve as impurezas
como amarras, grilhões, obstáculos e nós; por isso o caminho para o
desatamento, soltura e libertação, para o desamarrar dos nós, é ao mesmo tempo
uma disciplina dirigida à purificação interior. <o:p></o:p></span></p>

<p class=Normal>O trabalho de purificação
deve ser realizado no mesmo lugar em que as impurezas surgem, na própria mente
e o método principal que o Dhamma oferece para purificar a mente é a meditação.
A meditação, no treinamento Budista, não é nem uma busca pelo êxtase efusivo
nem uma técnica de psicoterapia caseira, mas um método cuidadosamente planejado
de desenvolvimento mental - preciso na teoria e eficiente na prática - para
alcançar a pureza interior e a libertação espiritual. As principais ferramentas
da meditação Budista são os fatores mentais saudáveis da energia, atenção
plena, concentração e entendimento. Mas na prática sistemática da meditação
eles são fortalecidos e conectados em um programa de auto purificação que tem
como objetivo extirpar as raízes e as ramificações das impurezas para que nem
mesmo o seu menor traço sutil possa restar. <o:p></o:p></span></p>

<p class=Normal>Já que todos os estados
impuros na mente nascem da ignorância que é a impureza mais profunda, a
purificação final e última da mente é alcançada através da instrumentação da
sabedoria, do conhecimento e visão das coisas como elas realmente são. A
sabedoria, no entanto, não surge por mero acaso ou por boas intenções
ocasionais, mas somente em uma mente purificada. Dessa forma para que a
sabedoria seja desenvolvida e a purificação plena seja alcançada através da
erradicação das impurezas, primeiro temos que criar um espaço, desenvolvendo
uma purificação provisória da mente - uma purificação que embora temporária e
vulnerável, é indispensável como uma base para que o insight libertador possa
surgir. <o:p></o:p></span></p>

<p class=Normal>Atingir essa purificação
preparatória da mente começa com o desafio do auto entendimento. Para eliminar
as impurezas precisamos primeiro aprender a conhecê-las, a detectá-las em ação
infiltrando-se e dominando os nossos pensamentos de todos os dias e as nossas
vidas. Por toda a eternidade temos agido baseado no impulso da cobiça, raiva e
delusão e por isso o trabalho de auto purificação não pode ser executado de
maneira apressada, em obediência à nossa demanda por resultados imediatos. A
tarefa requer paciência, cuidado e persistência - e as claras instruções do
Buda. Para cada impureza, o Buda com toda sua compaixão nos proporcionou o
antídoto, o método para escapar dela e vencê-la. Aprendendo esses princípios e
aplicando-os de maneira adequada podemos gradualmente fazer desaparecer aquelas
impurezas interiores mais persistentes e alcançar o fim do sofrimento, a
&quot;libertação imaculada da mente.&quot; <o:p></o:p></span></p>

