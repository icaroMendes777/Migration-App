<p class=Normal>O termo em Pali para meditação é <i>bhavana:</i> desenvolvimento. Esse é
um termo taquigráfico para o desenvolvimento de qualidades hábeis na mente.
Bhavana é um tipo de karma – a atividade intencional que no final das contas
conduz ao fim de karma – mas ainda assim é karma. Este ponto é enfatizado por
um outro termo em Pali para a meditação: <i>kammatthana,</i> a tarefa em mãos;
e por uma expressão em Tailandês para meditação: “fazer um esforço.” Vale a
pena manter esses termos presentes na mente, para contrabalançar a concepção
usual de que a meditação é um exercício de inação ou de aceitação passiva,
universal. Na verdade, da forma como é descrita nos textos em Pali, a meditação
é um processo que envolve bastante iniciativa. Esta possui uma agenda e se
trabalha ativamente para realizá-la. Isso pode ser visto na descrição em Pali
sobre como a atenção plena correta é cultivada através de <i>satipatthana</i>. <o:p></o:p></span></p>

<p class=Normal>Satipatthana com freqüência
é traduzido como “fundamento da atenção plena,” o que dá a impressão de que se
refere a um objeto de meditação. Essa impressão é reforçada quando você vê os
quatro satipatthanas listados como corpo, sensações, mente e objetos mentais.
Mas se você olhar os textos, verá que<span style="mso-spacerun: yes"> 
</span>eles ensinam satipathana como um processo, um modo de estabelecer, (<i
style='mso-bidi-font-style:normal'>upatthana)</i>, atenção plena, <i
style='mso-bidi-font-style:normal'>(sati)</i>: daí o termo composto. Quando os
textos definem esse composto, eles mencionam não uma lista de objetos, mas
quatro fórmulas que descrevem uma atividade. <o:p></o:p></span></p>

<p class=Normal>Abaixo está a primeira
fórmula:<o:p></o:p></span></p>

<p class=Normal style='margin-right:.5in;mso-margin-top-alt:auto;mso-margin-bottom-alt:
auto;margin-left:.75in'>Um
meditador permanece contemplando o corpo como um corpo, ardente, plenamente
consciente e com atenção plena, tendo colocado de lado a cobiça e o desprazer
pelo mundo. <o:p></o:p></span></p>

<p class=Normal>Cada um dos termos nesta
fórmula é importante. “Permanece contemplando” também pode ser traduzido como
“mantendo o foco.” Isto se refere ao elemento de concentração dentro da
prática, quando você se atém a um tema em particular dentre os fluxos
conflitantes da experiência. “Ardente” se refere ao esforço que você dedica à
prática, tentando abandonar os estados mentais inábeis e desenvolver estados
hábeis no lugar destes, ao mesmo tempo que tenta discernir a diferença entre os
dois. “Plenamente consciente” ou alerta, significa estar plenamente inteirado
daquilo que está acontecendo no presente. “Com atenção plena” significa ser
capaz de se lembrar ou recordar. Algumas vezes a atenção plena é traduzida como
uma atenção não reativa, isenta de agendas, simplesmente presente com qualquer
coisa que surja, mas a fórmula para satipatthana não endossa essa tradução. A
atenção não reativa faz parte na verdade da equanimidade, uma das muitas
qualidades fomentadas no processo de satipatthana, mas a ardência envolvida em
satipatthana definitivamente possui uma agenda, uma tarefa a ser feita, enquanto
o papel da atenção plena é manter essa tarefa presente na mente.<o:p></o:p></span></p>

<p class=Normal>A tarefa neste caso é
dupla: permanecer focado no fundamento e colocar de lado qualquer cobiça ou
desprazer que resultariam da transferência do fundamento de volta para o mundo.
Esse é o significado de “o corpo como um corpo.” Em outras palavras, você tenta
permanecer com a experiência do corpo tal como este é sentido de imediato, sem
referí-lo às narrativas e idéias que compõem a sua noção do mundo. Você
permanece afastado das histórias sobre como você se relacionou com o seu corpo
no passado e como espera se relacionar no futuro. Você abandona toda e qualquer
preocupação em relação a como o seu corpo se encaixa no mundo em termos da sua
beleza, agilidade ou força. Você simplesmente sintoniza o corpo de acordo com
as condições deste – a experiência direta da respiração, dos movimentos, das
posturas, das suas propriedades elementares e da sua inevitável decadência.
Dessa forma, você aprende a desnudar as suas suposições sobre o que está ou não
está por detrás da sua experiência do corpo e obtém proficiência em relacionar
tudo com a própria experiência. <o:p></o:p></span></p>

<p class=Normal>A mesma abordagem se aplica
aos demais tipos de satipatthana: focando nas sensações, na mente e nos objetos
mentais em si mesmos. À primeira vista, estes podem parecer exercícios de
meditação novos e diferentes, mas o Buda deixa claro que todos podem estar
centrados numa única prática: estar atento à respiração. Quando a mente está
com a respiração, todos os quatro fundamentos estarão exatamente ali. A
diferença está só na sutileza do seu foco. Assim quando você desenvolveu a
habilidade com o tipo de satipatthana mais evidente, você não terá que se mover
muito para se ocupar com os tipos mais sutis. Permaneça simplesmente com a
respiração e mova o seu foco para as sensações e estados mentais que surgem ao
estar atento à respiração e para os objetos mentais que ou atrapalham, ou
fortalecem o seu foco. Uma vez que você tenha escolhido o seu fundamento, você
o trata da mesma forma como tem tratado o corpo: tomando-o como um fundamento
em si mesmo, sem referí-lo às histórias sobre você mesmo ou as suas idéias
acerca do mundo. Você separa as sensações – de prazer, dor, ou nem prazer, nem
dor – das histórias com as quais você normalmente as circunda. Você separa os
estados de cobiça, raiva e delusão dos seus pontos focais no mundo. Assim, você
poderá vê-los como eles realmente são. <o:p></o:p></span></p>

<p class=Normal>No
entanto, você ainda tem uma agenda baseada na aspiração pela Iluminação -<span
style="mso-spacerun: yes">  </span>um desejo que o Buda classificou não como
uma causa para o sofrimento, mas como parte do caminho que conduz ao fim deste.
Isto se torna mais evidente no satipatthana focado nos objetos mentais em si
mesmos. Você se familiariza com as qualidades inábeis que obstruem a
concentração - tal como o desejo sensual, a má vontade e a inquietação - não só
pela experiência, mas também para compreendê-los e poder eliminá-los. De modo
semelhante, você se familiariza com as qualidades hábeis que estimulam a
sabedoria para poder desenvolvê-las em sua plenitude até alcançar a
libertação.<o:p></o:p></span></p>

<p class=Normal>Os textos chamam essas
qualidades hábeis de os sete fatores da Iluminação e mostram que a prática de
satipatthana tem como objetivo desenvolvê-los numa seqüência. O primeiro fator
é a atenção plena. O segundo é chamado “investigação dos fenômenos”: a habilidade
para distinguir as qualidades mentais hábeis das inábeis, ver aquilo que pode
ser aceito e aquilo que precisa ser mudado. O terceiro fator é a energia – a
energia para abandonar as qualidades inábeis e em seu lugar estimular as qualidades
hábeis. Os textos descrevem uma variedade de métodos para serem usados nesse
intento, mas todos eles se resumem em dois tipos. Em alguns casos, uma
qualidade inábil irá desaparecer só de observá-la com diligência. Em outros
casos, você terá que fazer um esforço concentrado, tomar uma ação enérgica,
necessária para contrapor uma qualidade inábil e substituí-la por uma qualidade
mais hábil. <o:p></o:p></span></p>

<p class=Normal>À medida que as qualidades
hábeis forem assumindo o controle no seu íntimo, você verá que embora o
pensamento hábil conduza a ações não prejudiciais, mantê-lo por longos períodos
de tempo pode cansar a mente. Assim, você silencia os seus pensamentos, o que
desenvolve mais três dos fatores da iluminação: êxtase, tranqüilidade e
concentração. Eles proporcionam à mente uma base de bem estar.<o:p></o:p></span></p>

<p class=Normal>O último fator é a
equanimidade e o seu posicionamento na lista é significativo. A sua
característica de passividade é plenamente apropriada apenas quando os fatores
mais ativos realizaram o que são capazes. Isso é verdade em todas as listas nas
quais a equanimidade é incluída. Ela nunca é mencionada sozinha, como requisito
suficiente para a Iluminação; e ela sempre ocorre ao final, depois dos seis
fatores antecedentes que constam da lista. Isso não significa que a
equanimidade supera os demais fatores, mas simplesmente que ela se associa na
interação deles. Ao invés de substituí-los, ela os contrabalança, permitindo
que você dê um passo atrás e veja níveis sutis de sofrimento e desejo que os
fatores anteriores possam ter obscurecido; criando assim espaço para que esses
fatores possam agir sobre os níveis recém-descobertos. Apenas quando todos os
níveis de sofrimento e desejo tiverem sido eliminados, a tarefa de ambos os
aspectos da meditação, iniciativa e passividade, terá sido concluída. É nesse
ponto que a mente estará verdadeiramente livre de agendas.<o:p></o:p></span></p>

<p class=Normal>É como aprender a tocar
piano. À medida que você se torna mais competente, tocando com grande
habilidade, você também desenvolve sensibilidade para ouvir de forma passiva,
discernindo níveis cada vez mais sutis na música. E isso faz com que você toque
com maior habilidade ainda. Da mesma forma, à medida que você se torna mais
hábil no estabelecimento da atenção plena no fundamento que escolheu, você
obtém maior sensibilidade para remover as camadas cada vez mais sutis do
momento presente até que nada mais reste obstruindo o caminho para a completa
libertação. <o:p></o:p></span></p>




