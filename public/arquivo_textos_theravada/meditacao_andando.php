<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Nos nossos
retiros de meditação, os iogues praticam a atenção plena em quatro posturas
distintas. Eles praticam a atenção plena ao caminhar, em pé, sentados e quando
se deitam. Eles precisam manter a atenção plena em todos os momentos em
qualquer postura que estejam. A postura mais comum para a meditação da atenção
plena é sentado com as pernas cruzadas, mas como o corpo humano não é capaz de
tolerar essa posição por muitas horas, nós alternamos períodos de meditação
sentada com períodos de meditação andando. Visto que a meditação andando é
muito importante, eu gostaria de discutir a sua natureza, sua importância e os
benefícios derivados da sua prática. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A prática da meditação da
atenção plena pode ser comparada com o ato de ferver água. Se alguém quer
ferver água, primeiro coloca a água na chaleira. Coloca a chaleira sobre o
queimador do fogão e depois acende o fogo. Mas se a chama for apagada, mesmo
que seja apenas por um instante, a água não irá ferver, mesmo que a chama seja
acesa novamente mais tarde. Se a pessoa ficar acendendo e apagando a chama o
tempo todo, a água nunca irá ferver. Da mesma forma, se existem intervalos
entre os momentos de atenção plena, a pessoa não obterá impulsão e assim não
obterá a concentração. É por isso que os iogues nos nossos retiros são
instruídos a praticar a atenção plena durante todo o tempo em que estejam
despertos, do momento em que acordem pela manhã até quando adormeçam à noite.
Por conseguinte, a meditação andando é parte integral do processo contínuo do
desenvolvimento da atenção plena. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Infelizmente, tenho ouvido
pessoas criticarem a meditação andando, argumentando que elas não conseguem
obter dela nenhum benefício ou resultado. Mas foi o próprio Buda quem pela
primeira vez ensinou a meditação andando. No </span><i><a
href="../sutta/MN10.php"><span lang=PT-BR style='mso-ansi-language:PT-BR'>Discurso
dos Fundamentos da Atenção Plena</span></a></i><i><span lang=PT-BR
style='mso-ansi-language:PT-BR'>, </span></i><span lang=PT-BR style='mso-ansi-language:
PT-BR'>o Buda ensinou a meditação andando duas vezes. Na seção intitulada
“Posturas,” ele disse que um bhikkhu sabe “Eu estou andando” quando ele está
andando, sabe “Eu estou em pé” quando está em pé, sabe “Eu estou sentado”quando
está sentado e sabe “Eu estou deitado” quando está deitado. Em uma outra seção
chamada “Plena Consciência,” o Buda disse, “um bhikkhu age com plena
consciência quando vai para a frente e quando retorna.” <i>Plena Consciência </i>significa
o correto entendimento daquilo que é observado. Para entender corretamente
aquilo que é observado um iogue necessita obter concentração e para obter
concentração, ele precisa aplicar a atenção plena. Portanto, quando o Buda
disse, “Bhikkhus, empreguem a plena consciência,” precisamos entender que não
somente a plena consciência deve ser empregada, mas também a atenção plena e a
concentração. Dessa forma o Buda estava instruindo os meditadores a empregar a
atenção plena, a concentração e a plena consciência ao caminhar, ao “ir para a
frente e retornar.” A meditação andando é portanto uma parte importante desse
processo.<span style='mso-spacerun:yes'>  </span><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Embora não esteja
registrado no sutta que o Buda tenha dado instruções detalhadas e específicas
para a meditação andando, acreditamos que ele deva ter dado essas instruções em
algum momento. Essas instruções devem ter sido aprendidas pelos seus discípulos
e transmitidas ao longo de sucessivas gerações. Além disso, os mestres dos
tempos antigos devem ter formulado instruções baseadas na sua própria prática.
Na atualidade, temos um conjunto detalhado de instruções sobre como praticar a
meditação andando. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Falemos agora
especificamente sobre a prática da meditação andando. Se você for um iniciante,
o mestre poderá instruí-lo a colocar a atenção plena em apenas uma coisa
durante a prática da meditação andando: estar plenamente atento ao ato de dar o
passo e ao mesmo tempo fazendo silenciosamente uma notação mental, “andando,
andando, andando,” ou “esquerdo, direito, esquerdo, direito.” Você<span
style='mso-spacerun:yes'>  </span>deveria caminhar num ritmo abaixo do normal
durante essa prática.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Após algumas horas, ou após
um dia ou dois de meditação, você poderá ser instruído para colocar a atenção
plena em dois eventos: (i) dar o passo e (ii) abaixar o pé, ao mesmo tempo
fazendo a notação mental “andando, abaixando.” Você deverá tentar ficar atento
a dois estágios no processo de caminhar: “dar o passo, abaixar; dar o passo,
abaixar.” Mais tarde, você poderá ser instruído para colocar a atenção plena em
três estágios:<span style='mso-spacerun:yes'>  </span>(i) levantar o pé; (ii)
mover ou empurrar o pé para a frente; e (iii) abaixar o pé. Mais tarde ainda,
você poderá ser instruído para colocar a atenção plena em quatro estágios de cada
passo: (i) levantar o pé; (ii) movê-lo para a frente; (iii) abaixá-lo; e (iv)
tocar ou pressionar o pé no chão. Você será instruído para estar completamente
atento e para fazer uma notação mental desses quatro estágios do movimento do
pé: “levantar, mover para a frente, abaixar, pressionar o chão.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>No início os iogues acham
difícil andar mais devagar, mas como eles são instruídos a prestar bastante
atenção a todos os movimentos envolvidos, e assim que eles realmente passem a
prestar cada vez mais atenção, eles automaticamente desaceleram. Eles não
precisam ser compelidos a desacelerar, pois à medida que eles prestem mais
atenção, andar mais devagar lhes ocorre de forma automática. Ao dirigir<span
style='mso-spacerun:yes'>  </span>numa auto estrada, uma pessoa pode estar
andando a cem ou cento e dez ou cento e vinte km por hora. Andando nessa
velocidade ela não será capaz de ler alguns dos sinais na estrada. Se a pessoa
quiser ler os sinais, terá que reduzir a velocidade. Ninguém precisa dizer,
“Desacelere!”<span style='mso-spacerun:yes'>  </span>o motorista irá reduzir a
velocidade de forma automática para poder ler os sinais. Da mesma forma, se os
iogues quiserem prestar mais atenção aos movimentos de levantar, mover para a
frente, abaixar e pressionar o solo, eles irão desacelerar de forma automática.
Somente quando eles diminuírem a velocidade poderão estar verdadeiramente
atentos e plenamente conscientes desses movimentos.<span
style='mso-spacerun:yes'>  </span><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Embora os iogues prestem
bastante atenção e reduzam a velocidade, pode ser que eles não vejam todos os
movimentos e estágios com clareza. Os estágios podem não estar ainda bem
definidos na mente e pode parecer que eles constituam apenas um movimento
contínuo. À medida que a concentração for se tornando mais firme, os iogues
irão observar cada vez com mais clareza os diferentes estágios em cada passo;
pelo menos os quatro estágios serão distinguidos com mais facilidade. Os iogues
saberão com clareza que o movimento de levantar não está misturado com o
movimento de levar adiante, e eles saberão que o movimento de levar adiante não
está misturado como o movimento de levantar nem com o movimento de pressionar.
Eles compreenderão todos os movimentos com clareza e nitidez. Tudo aquilo que
tiver sido alvo da atenção plena e consciência estará bastante nítido nas suas
mentes. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>À medida que os iogues
continuam com a prática, eles passarão a observar muito mais. Quando levantarem
o pé, experimentarão a leveza do pé. Quando empurrarem o pé para<span
style='mso-spacerun:yes'>  </span>frente, irão notar o movimento de um lugar
para outro. Quando abaixarem o pé, eles sentirão o peso do pé, porque o pé fica
cada vez mais pesado à medida que desce. Quando eles colocarem o pé no chão,
sentirão o toque do calcanhar no chão. Então, junto com a observação de
levantar, mover adiante, abaixar e pressionar o chão, os iogues também
perceberão a leveza do pé sendo levantado, o movimento do pé, o peso do pé
sendo baixado e depois o toque no pé, que é a suavidade ou dureza do pé no
chão. Quando os iogues percebem esses processos, ele estão percebendo os quatro
elementos essenciais (em Pali, <i>dhatu).</i> Os quatro elementos essenciais
são: o elemento terra, o elemento água, o elemento fogo e o elemento ar. Ao
prestar bastante atenção a esses quatro estágios da meditação andando, os
quatro elementos na sua verdadeira essência são percebidos, não como meros
conceitos, mas como processos verdadeiros, como realidades últimas. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Analisemos um pouco mais em
detalhe as características dos elementos na meditação andando. No primeiro
movimento, levantar o pé, os iogues percebem a leveza, e ao perceber a leveza
eles em essência percebem o elemento fogo. Um aspecto do elemento fogo é fazer
com que as coisas fiquem mais leves e quando as coisas ficam mais leves, elas
sobem. Na percepção da leveza no movimento ascendente do pé, os iogues percebem
a essência do elemento fogo. Mas no levantar o pé existe também, além da
leveza, movimento. Movimento é um aspecto do elemento ar. Mas a leveza, o
elemento fogo, é dominante, portanto podemos dizer que no estágio de levantar o
pé o elemento fogo é o primário e o elemento ar, o secundário. Esses dois elementos
são percebidos pelos iogues ao prestar bastante atenção ao movimento de
levantar o pé. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O próximo estágio é mover o
pé para a frente. Ao mover o pé para a frente, o elemento dominante é o
elemento ar, porque o movimento é uma das principais características do
elemento ar. Portanto, ao prestar bastante atenção ao movimento do pé para a
frente na meditação andando, os iogues estão na prática percebendo a essência
do elemento ar. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O próximo estágio é o
movimento de abaixar o pé. Quando os iogues abaixam o pé existe uma espécie de
peso no pé. Peso é uma característica do elemento água, tal como gotejar e
ressumar. Quando um líquido é denso, ele ressuma. Portanto quando os iogues
percebem o peso do pé, eles percebem a essência do elemento água. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ao pressionar o pé no chão,
os iogues perceberão a dureza ou suavidade do pé no chão. Isso pertence à
natureza do elemento terra. Ao prestar bastante atenção à pressão do pé contra
o chão, os iogues irão na prática perceber a natureza do elemento terra. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Portanto, vemos que em
apenas um passo os iogues podem perceber muitos processos. Eles podem perceber
os quatro elementos e a natureza dos quatro elementos. Somente aqueles que
praticam podem ter a esperança de ver essas coisas. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>À medida que os iogues
continuam<span style='mso-spacerun:yes'>  </span>praticando a meditação
andando, eles irão compreender que com cada movimento existe também a mente que
registra, a consciência do movimento. Ocorre o movimento de levantar e também a
mente que tem consciência desse movimento de levantar. No momento seguinte, existe
o movimento para a frente e também a mente que tem consciência desse movimento.
Além disso, os iogues irão compreender que ambos, o movimento e a consciência,
surgem e desaparecem naquele momento. No momento seguinte, existe o movimento
de abaixar e também a consciência desse movimento, e ambos surgem e desaparecem
naquele momento de colocar o pé no chão. O mesmo processo ocorre com o
pressionar do pé: existe o pressionar e a consciência do pressionar. Dessa
forma, os iogues compreendem que junto com os movimentos do pé, também existem
os momentos da consciência. Os momentos da consciência são chamados em Pali, <i>nama,</i>
mente, e o movimento do pé é chamado <i>rupa,</i> matéria. Dessa forma os
iogues percebem a mente e a matéria surgindo e desaparecendo em cada momento.
Em um momento ocorre o levantar do pé e a consciência de levantar, e no momento
seguinte existe o movimento para a frente e a consciência desse movimento e
assim por diante. Eles podem ser compreendidos como um par, mente e matéria,
que surgem e desaparecem a cada momento. Assim os iogues avançam para a
percepção da ocorrência combinada da mente e matéria em cada momento de
observação, isto é, se eles prestarem bastante atenção. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Outra coisa que os iogues
irão descobrir é o papel da intenção ao efetuar cada movimento. Eles
compreenderão que levantam o pé porque querem fazer isso, movem o pé para
diante porque querem fazer isso, abaixam o pé porque querem fazer isso,
pressionam o pé no chão porque querem fazer isso. Isto é, eles compreendem que
a intenção precede cada movimento. Depois da intenção de levantar, ocorre o
levantamento. Eles passam a entender a condicionalidade de todas essas
ocorrências – esses movimentos nunca ocorrem por si mesmos, sem condições.
Esses movimentos não são criados por alguma divindade ou autoridade e esses
movimentos nunca ocorrem sem uma causa. Existe uma causa e condição para cada
movimento, e essa condição é a intenção que precede cada movimento. Essa é mais
uma descoberta que os iogues realizam se prestarem bastante atenção. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando os iogues
compreenderem a condicionalidade de todos os movimentos, e que esses movimentos
não são criados por nenhuma autoridade ou deus, então eles compreenderão que os
movimentos são criados pela intenção. Eles compreenderão que a intenção é a
condição para que o movimento ocorra. Dessa forma a relação entre o
condicionamento e o condicionado, de causa e efeito, será compreendida. Com
base nessa compreensão os iogues podem remover a dúvida sobre <i>nama</i> e <i>rupa</i>
ao compreender que <i>nama</i> e <i>rupa</i> não surgem sem condições. Com o
claro entendimento da condicionalidade das coisas e com a transcendência da
dúvida sobre <i>nama</i> e <i>rupa,</i> se diz que o iogue alcançou o estágio
de um “pequeno <i>sotapanna.</i> &quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Um <i>sotapanna</i> é
aquele “que entrou na correnteza,” uma pessoa que alcançou o primeiro nível de
iluminação. Um “pequeno <i>sotapanna&quot;</i> não é um verdadeiro <i>sotapanna
</i>mas se diz que ele tem assegurado o renascimento em um plano de existência
feliz, tal como os reinos dos seres humanos e<span style='mso-spacerun:yes'> 
</span><i>devas.</i> Isto é, um pequeno <i>sotapanna</i> não renasce num dos
estados miseráveis, num dos reinos animais ou do inferno. Esse estado de
pequeno <i>sotapanna </i>só<i> </i>pode ser alcançado através da prática da
meditação andando, prestando bastante atenção aos movimentos envolvidos em um passo.
Esse é o grande benefício da prática de meditação andando. Esse estágio não é
fácil de ser alcançado, mas uma vez que os iogues o alcancem, eles terão a
garantia de renascer em um destino feliz, exceto, é claro, se eles decaírem
desse estágio. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando os iogues
compreenderem a mente e a matéria surgindo e desaparecendo a cada momento, eles
irão então compreender a impermanência do processo de levantar o pé e eles
também compreenderão a impermanência da consciência desse movimento. A
ocorrência do desaparecimento após o surgimento é uma marca ou característica
pela qual compreendemos que algo é impermanente. Se queremos determinar se algo
é impermanente<span style='mso-spacerun:yes'>  </span>ou permanente, devemos
tentar ver, através do poder da meditação, se aquela coisa está ou não sujeita
ao processo de vir a ser e depois desaparecer. Se a nossa meditação for
suficientemente poderosa para nos permitir ver o surgimento e desaparecimento
dos fenômenos, então podemos concluir que os fenômenos observados são
impermanentes. Dessa forma, os iogues observam que existe o movimento de
levantar e a consciência desse movimento, e depois essa seqüência desaparece
dando origem ao movimento de empurrar para a frente e a consciência de empurrar
para a frente. Esses movimentos simplesmente surgem e desaparecem, surgem e
desaparecem, e os iogues podem compreender esse processo por si mesmos – eles
não precisam aceitar isso baseados na confiança em alguma autoridade externa,
nem precisam acreditar na naquilo que uma outra pessoa possa lhe dizer.<span
style='mso-spacerun:yes'>  </span><o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando os iogues
compreendem que a mente e a matéria surgem e desaparecem, eles compreendem que
a mente e a matéria são impermanentes.<span style='mso-spacerun:yes'> 
</span>Quando eles vêm que elas são impermanentes, em seguida eles compreendem
que elas são insatisfatórias porque estão sempre oprimidas pelo constante
surgimento e desaparecimento. Após compreender a natureza impermanente e
insatisfatória das coisas, eles observam que não é possível ter o domínio sobre
essas coisas; isto, é, os iogues compreendem que não existe um eu ou alma interna
que possa fazer com que elas sejam permanentes. As coisas simplesmente surgem e
desaparecem de acordo com a lei da natureza. Ao compreender isso, os iogues
compreendem a terceira característica dos fenômenos condicionados, a
característica de <i>anatta, </i>a característica que mostra que as coisas não
possuem um eu. Um dos significados de <i>anatta </i>é o não domínio –
significando que nada, nenhuma entidade, nenhuma alma, nenhum poder tem domínio
sobre a natureza das coisas. Assim, a esta altura, os iogues compreenderam as
três características de todos os fenômenos condicionados: impermanência,
sofrimento e o não eu - em Pali, <i>anicca,</i> <i>dukkha,</i> e <i>anatta.</i>
<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os iogues podem compreender
essas três características observando atentamente o mero levantar do pé e a
consciência do levantamento do pé. Ao prestar bastante atenção aos movimentos
eles podem ver as coisas surgindo e desaparecendo, e por conseguinte podem ver
por si mesmos a natureza impermanente, insatisfatória e não eu de todos os
fenômenos condicionados. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Examinemos agora em mais
detalhe os movimentos da meditação andando. Suponham que se filmasse o
levantamento do pé. Suponham também que o levantamento do pé demorasse um
segundo e digamos que a câmera filma trinta e seis quadros por segundo. Depois
de filmar, se olharmos os quadros separados, nos daremos conta que dentro do
que pensávamos ser um movimento de levantar, houve na verdade trinta e seis
movimentos. A imagem em cada quadro é ligeiramente distinta das imagens dos
demais quadros, embora a diferença seja tão sutil que mal possa ser notada. Mas
e se a câmera pudesse filmar mil quadros por segundo? Então haveriam mil
movimentos em apenas um movimento de levantar, embora fosse quase impossível
diferenciar os movimentos. Se a câmera pudesse filmar um milhão de quadros por
segundo – que é impossível na atualidade, mas que talvez algum dia seja – então
haveria um milhão de movimentos naquilo que pensávamos ser apenas um movimento.
<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Nosso esforço na meditação
andando é de ver os nossos movimentos com a mesma precisão com a qual a câmera
os vê, quadro a quadro. Também queremos observar a consciência e a intenção que
precede cada movimento. Também podemos apreciar o poder da sabedoria e insight
do Buda, por meio dos quais ele, na verdade, viu todos os movimentos. Quando
usamos a palavra “ver” ou “observar” para nos referirmos à nossa própria
situação, queremos dizer que vemos diretamente e também por inferência; talvez
não sejamos capazes de ver diretamente todos os milhões de movimentos tal como
o Buda. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Antes que os iogues comecem
a praticar a meditação andando, eles talvez pensem que um passo é apenas um
movimento. Após meditar sobre esse movimento, eles observam que existem pelo
menos quatro movimentos, e se eles se aprofundarem, irão compreender que mesmo
um desses quatro movimentos consiste de milhões de movimentos muito pequenos.
Eles vêm <i>nama</i> e <i>rupa,</i> mente e matéria, surgindo e desaparecendo,
como impermanentes. Através da nossa percepção comum, não somos capazes de ver
a impermanência das coisas porque a impermanência está escondida pela ilusão da
continuidade. Pensamos que vemos apenas um movimento contínuo, mas se olharmos
com cuidado veremos que a ilusão da continuidade pode ser rompida. Pode ser
rompida através da observação direta dos fenômenos físicos parte por parte,
segmento por segmento, como eles se originam e se desintegram. O valor da
meditação está na nossa habilidade em remover o manto da continuidade de forma
a descobrir a verdadeira natureza da impermanência. Os iogues podem descobrir a
natureza da impermanência diretamente, através do seu próprio esforço.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Depois de compreender que
as coisas são compostas de segmentos, de que elas ocorrem em partes e depois de
observar esses segmentos um a um, os iogues irão compreender que realmente não
existe nada neste mundo a que se apegar, nada a cobiçar. Se virmos<span
style='mso-spacerun:yes'>  </span>algo, que um dia consideramos belo, com
falhas, decaindo e desintegrando, perderemos interesse por ele. Por exemplo,
podemos ver um bela pintura numa tela. Pensamos na pintura e na tela
conceitualmente como um todo, uma coisa sólida. Mas se colocássemos a pintura
sob um microscópio poderoso, veríamos que a pintura não é sólida – ela possui
muitos buracos e espaços. Depois de ver que a pintura está composta em grande
parte por espaços, perderíamos interesse por ela e abandonaríamos o nosso apego
por ela. Os físicos modernos conhecem bem essa idéia. Eles observaram, através
de instrumentos poderosos, que a matéria não passa de partículas vibrando e
energia em constante mutação – não existe<span style='mso-spacerun:yes'> 
</span>nada de sólido nela. Compreendendo essa impermanência sem fim, os iogues
compreendem que não existe realmente nada que cobiçar, nada que se apegar em
todo o mundo dos fenômenos. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Agora podemos compreender
as razões para a prática da meditação. Praticamos meditação porque queremos
remover o apego e a cobiça pelos objetos. É compreendendo as três
características da existência – impermanência, sofrimento e o não eu – que
removemos a cobiça. Queremos remover a cobiça porque queremos deixar de sofrer.
Enquanto existir a cobiça e o apego haverá sempre o sofrimento. Se não
quisermos sofrer, precisamos remover a cobiça e o apego. Precisamos compreender
que todas as coisas são apenas mente e matéria surgindo e desaparecendo, que as
coisas não possuem substância. Uma vez que compreendamos isso, seremos capazes
de remover o nosso apego às coisas. Enquanto não compreendermos isso, não
importa quantos livros leiamos, ou palestras ouçamos, ou o quanto falemos a
respeito da remoção do apego, não seremos capazes de remover o apego. É necessário
ter a experiência direta de que todas as coisas condicionadas possuem as marcas
das três características. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Portanto precisamos prestar
bastante atenção quando estamos caminhando, igual quando estivermos sentados ou
deitados. Não estou dizendo que apenas a meditação andando possa nos
proporcionar a realização última e a habilidade para remover o apego
completamente, mas ela é uma prática válida tal como a meditação sentada ou
qualquer outro tipo de meditação <i>vipassana</i> (insight). A meditação
andando conduz ao desenvolvimento espiritual. É tão poderosa quanto a atenção
plena na respiração ou a atenção plena na expansão e contração do abdomen. É
uma ferramenta eficiente para nos auxiliar na remoção das impurezas mentais. A
meditação andando pode nos ajudar a obter o insight da natureza das coisas, e
por isso deveríamos praticá-la com a mesma diligência com que praticamos a
meditação sentada ou qualquer outro tipo de meditação.<span
style='mso-spacerun:yes'>      </span><o:p></o:p></span></p>

