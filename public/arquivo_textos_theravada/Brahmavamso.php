<p class=Tit1 align=center style='text-align:center'>Ajaan Brahmavamso</Tit1>

<p class=Normal>Ajaan Brahmavamso (também conhecido como Ajaan Brahm) (Peter Betts) nasceu em Londres em 1951. Ele teve uma origem humilde e já aos 17 anos de idade se considerava um Budista através dos livros que leu enquanto freqüentava a escola. Ele obteve uma bolsa de estudos para a Universidade de Cambridge onde estudou Física, durante esse período cresceu o seu interesse pela meditação. Depois de formado com um BA em Física e de lecionar matemática e ciências num colégio em Devon durante um ano, ele perdeu o encantamento por aquilo que havia estudado porque sentia que todos os grandes cientistas sabiam muitas coisas sobre o universo, mas praticamente nada acerca das sua próprias mentes. Assim ele resolveu ir para a Tailândia, onde aos 23 anos de idade foi ordenado em Bangcoc por aquele que mais tarde se tornaria o cabeça de toda a Sangha Tailandesa, Phra Somdet Buddhajahn, que na época era o abade do Wat Saket, um dos templos mais famosos em Bangcoc. Depois de alguns meses em Wat Saket ele foi para o Wat Pa Pong e pouco depois para o Wat Pa Nanachat que havia sido criado recentemente. Durante 9 anos ele estudou e treinou na tradição das florestas do renomado mestre de meditação Ajaan Chah.<o:p></o:p></span></p>

<p class=Normal>Em 1983, foi convidado pela Buddhist Society of Western Australia a ajudar Ajaan Jagaro na criação de um monastério de florestas próximo a Perth, no Oeste da Austrália. Em 1993 ele substituiu Ajaan Jagaro como abade do monastério. O monastério, Bodhinyana Monastery, conta com aproximadamente 20 monges residentes.  Ele também é o Diretor Espiritual da Buddhist Society of W.A (Dhammaloka Buddhist Centre), Conselheiro Espiritual da Buddhist Society of Victoria e Diretor Espiritual do Cittabhavana Buddhist Hermitage em Bundanoon, N.S.W, e por fim ele é também o Patrono Espiritual da Buddhist Fellowship’s.<o:p></o:p></span></p>

<p class=Normal>Em 1997 foi estabelecido um monastério para mulheres, (Dhammasara Nun’s Monastery), que durante muito tempo esteve sob a liderança da Ven. Ajaan Vayama. Em 2009 as 4 siladharas residentes no monastério foram ordenadas como bhikkhunis, sendo que essa foi a primeira ordenação de bhikkhunis na tradição das florestas.<o:p></o:p></span></p>


<p class=Normal>Em 2003 ele esteve num retiro isolado por 6 meses. Durante esse período ele não teve contato com nenhuma pessoa, dedicando-se exclusivamente à meditação e ao estudo ocasional de algum sutta do Majjhima Nikaya.<o:p></o:p></span></p>

<p class=Normal>Em 2006 Ajaan Brahm foi agraciado com a honraria eclesiástica Tailandesa de Than Chao Kun com o título de Phra Visuddhisamvarathera.</p>

<p class=Normal>Em 2009 sob a direção de Ajaan Brahm foi concluída a construção do Jhana Grove um centro dedicado a retiros de meditação, próximo ao Bodhinyana Monastery.</p>

<p class=Normal>Ajaan Brahm visita com regularidade as prisões no Oeste da Austrália para ensinar Budismo e meditação. Viajando com freqüência, Ajaan Brahm dá palestras e dirige retiros de meditação tanto na Austrália como em outros países da Ásia.<o:p></o:p></span></p>

<p class=Normal>Ele é o autor de <i>Mindfulness, Bliss, and Beyond: A Meditator’s Handbook</i>, <i>Who Ordered this Truckload of Dung (traduzido para o Português - Antes que o dia acabe, seja feliz!)</i> e <i>The Art of Disappearing</i>.</p>

<p class=Normal>Ajaan Brahm possui incansável energia para o ensino do Dhamma. Ele emprega uma singular combinação de erudição, meditação e um senso de humor travesso para comunicar os ensinamentos Budistas para uma ampla e variada audiência.<o:p></o:p></span></p>



