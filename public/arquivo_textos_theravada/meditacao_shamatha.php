<p class=Normal><b>Visão Geral do Projeto Shamatha</b>

<p class=Normal>Sob a direção do Dr. Clifford Saron, principal pesquisador do Center for Mind and Brain - UC Davis, e do erudito Budista B. Alan Wallace, o Projeto Shamatha está explorando como o treinamento intensivo de três meses na prática de meditação afeta a cognição, o comportamento e a fisiologia. </p> 

<p class=Normal>A meditação proporciona muitos benefícios para a saúde mental e física. O Projeto Shamatha, até o momento, o estudo mais abrangente da meditação, investiga os processos psicológicos e fisiológicos que fundamentam esses benefícios. Num estudo randômico, controlado, estudamos como o treinamento intensivo na meditação afeta como as pessoas pensam e sentem. Empregamos tarefas cognitivas e perceptivas, provocações emocionais, questionários e monitoramento fisiológico e bioquímico para avaliar as habilidades e comportamento das pessoas antes, durante e depois da prática de meditação intensiva a longo prazo. </p>

<p class=Normal><b>I. O Design do Estudo</b></p>

<p class=Normal>Um conjunto de 60 pessoas saudáveis, com experiência na meditação, foram separadas de modo aleatório, num grupo para um retiro de meditação intensivo de 3 meses ou num grupo de controle. Os participantes do grupo de controle mais tarde também participaram de um retiro de 3 meses. Avaliações de laboratório foram feitas de todos participantes antes, durante e depois dos retiros e em vários pontos de follow-up. No retiro os participantes foram instruídos por B. Alan Wallace nas práticas meditativas destinadas a promover o relaxamento, aprimorar a atenção e desenvolver a compaixão e bondade em relação aos outros. Os participantes praticaram sozinhos 6 horas por dia, durante 3 meses. </p>

<p class=Normal><b>II. Resumo das Descobertas até o Momento</b> (Base 15 de Setembro de 2010) </p>

<p class=Normal>Os resultados iniciais mostraram que o treinamento intensivo na meditação apura e sustenta a atenção, melhora o bem-estar e conduz a uma resposta emotiva ao sofrimento dos outros com menos crítica e mais empatia. Além disso o treinamento esteve conectado com o comportamento emocional pró-social e importantes indicadores fisiológicos de boa saúde. </p>

<p class=Normal><b>Atenção</b>. Como melhorou a atenção? A sensibilidade perceptual dos participantes em ambos retiros melhorou, avaliada pela sua habilidade em detectar diferenças mínimas em linhas com distintos comprimentos Esse tipo de melhoria foi mantida por no mínimo 5 meses depois dos retiros naquelas pessoas que deram continuidade à prática de meditação. Também, aqueles que receberam o treinamento mantiveram melhor o foco na mesma tarefa na medida em que o retiro progredia. </p>

<p class=Normal>A capacidade para inibir respostas também melhorou com o treinamento, demonstrado por participantes serem capazes de conter a reação a um estímulo visual de uma linha, quando esta algumas vezes parecia ter o comprimento mais curto. Essa melhoria prognosticou outros benefícios, avaliados através de questionários, avaliando as formas positivas e negativas de relacionamento consigo mesmo e com os outros. </p>

<p class=Normal><b>Emoções e Bem-estar</b>. Houve substanciais melhorias no bem-estar psicológico durante o retiro - melhorias que se mantiveram por pelo menos 5 meses depois do treinamento. De acordo com os relatórios pessoais diários do estado de espírito, os participantes em ambos retiros experimentaram ao longo do tempo melhorias de bem-estar e um intensificado senso de maravilhamento. </p>

<p class=Normal>Os participantes também receberam tarefas de laboratório sobre o funcionamento emocional. Eles assistiram filmes de uma guerra recente com cenas de violência, retratando ambos os perpetradores e as vítimas dos atos de violência. A análise das expressões faciais, filmadas discretamente, dessas cenas de violência, revelaram mais expressões de tristeza no grupo do retiro do que no grupo de controle, o que nós interpretamos como relevante para a compaixão. Verificamos modos distintos de resposta ao sofrimento nos dois grupos, sugerindo menos aversão e mais envolvimento por parte daqueles que participaram do retiro. </p>

<p class=Normal><b>Indicadores Biológicos</b>. Avaliamos alguns indicadores biológicos que podem mudar como resultado do treinamento meditativo. Um desses indicadores, telomerase, uma enzima que protege o material genético durante a divisão das células e que incrementa a viabilidade da célula, pode ser suprimida em resposta à aflição psicológica. Amostras de sangue obtidas ao final do retiro revelaram que a atividade da telomerase estava significativamente mais elevada nos participantes do retiro, (versus grupo de controle), e que a atividade da telomerase estava relacionada às mudanças no bem-estar induzidas pela meditação. </p>

