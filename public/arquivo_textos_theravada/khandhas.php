<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A iluminação
do Buda proporcionou-lhe entre outras coisas, um novo ponto de vista com
relação aos usos e limitações das palavras. Ele descobriu uma nova realidade –
o Imortal – que não pode ser descrita por palavras. Ao mesmo tempo, ele
descobriu que o caminho para a iluminação <i style='mso-bidi-font-style:normal'>pode
</i>ser descrito, embora envolva uma nova maneira de ver e conceituar o
problema do sofrimento e estresse. Como os conceitos comuns eram com freqüência
meios pobres para ensinar o caminho, ele teve que inventar novos conceitos
e<span style="mso-spacerun: yes">  </span>expandir palavras já existentes para
abranger esses conceitos de modo que outros pudessem experimentar a iluminação
por si mesmos. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Um dos novos conceitos, que
é essencial dentro dos seus ensinamentos, foi este dos <i style='mso-bidi-font-style:
normal'>khandhas</i><span style='mso-bidi-font-style:italic'>,</span> mais
freqüentemente traduzido para o Português como “agregados.” Antes do Buda, a
palavra em Pali <i style='mso-bidi-font-style:normal'>khandha </i>possuía
significados bastante comuns: Um khandha podia ser uma pilha, um maço, um
amontoado, uma massa. Também poderia ser o tronco de uma árvore. No seu
primeiro sermão, no entanto, o Buda lhe deu um significado novo, psicológico,
introduzindo o termo “khandhas influenciados pelo apego” para sumariar a sua
análise da verdade do sofrimento e estresse. Ao longo do restante da sua
carreira de ensino, ele se referiu freqüentemente a esses khandhas
psicológicos. A sua importância nos seus ensinamentos, desde então, tem sido
óbvia para cada geração de Budistas. Menos óbvia, no entanto, é a questão sobre
<i style='mso-bidi-font-style:normal'>como </i>eles são importantes: Como um
meditador deveria fazer uso do conceito dos khandhas psicológicos? Quais
questões eles deveriam responder?<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A resposta mais comum a
essas questões é melhor exemplificada através de dois livros acadêmicos
recentes dedicados ao tema. Ambos tratam os khandhas como a resposta do Buda à
questão, “O que é uma pessoa?” Citação da sobrecapa do primeiro: <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Se o Budismo nega um eu permanente, como é entendida a individualidade?
... Aquilo que por convenção chamamos de uma pessoa pode ser compreendido como
os cinco agregados, cuja soma não deve ser tomada como uma entidade permanente,
visto que os seres não são nada mais que um amálgama de fenômenos em constante
mutação ... Sem o cabal entendimento dos cinco agregados, não podemos
compreender o processo de libertação em operação dentro do indivíduo, que é, no
final das contas, simplesmente um amálgama dos cinco agregados.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Citação da introdução do
segundo: <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> “O terceiro ensinamento chave é dado pelo Buda num contexto onde ele é
perguntado acerca da identidade individual: quando as pessoas querem saber ‘o
que sou eu?’, ‘qual é o meu eu <i style='mso-bidi-font-style:normal'>real?’. </i>O
Buda diz que a individualidade deve ser compreendida como uma combinação de
fenômenos que formam o contínuo físico e mental de uma vida individual. Nesse
contexto, o ser humano é analisado em cinco componentes - os <i>pañcakkhandha</i>
[cinco agregados].” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Esse entendimento dos
khandhas não está confinado aos acadêmicos. Quase todos os professores de
meditação Budista modernos explicariam os khandhas de forma semelhante. E essa
não é uma inovação moderna. Foi primeiro proposto no início da era Cristã nos
comentários dos antigos cânones Budistas – ambos, do Theravada e do
Sarvastivada, que formaram a base para o escolasticismo Mahayana. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>No entanto, uma vez que os
comentários empregaram os khandhas para definir o que é uma pessoa, eles
geraram muitas das controvérsias que desde então têm atormentado o pensamento
Budista: “Se uma pessoa é apenas khandhas, então o que renasce?” “Se uma pessoa
é apenas khandhas, e os khandhas são aniquilados ao alcançar o nibbana total,
então o nibbana total não seria a aniquilação da pessoa?” “Se uma pessoa é
khandhas, e os khandhas estão inter-relacionados com outros khandhas, como é
possível que uma pessoa alcance nibbana sem arrastar consigo todas as demais
pessoas?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Uma grande parte da
história do pensamento Budista tem sido a história de tentativas engenhosas mas
infrutíferas de resolver essas questões. É instrutivo observar, no entanto, que
o cânone em Pali nunca menciona o Buda tentando respondê-las. Na verdade, o
cânone nunca menciona o Buda tentando definir de maneira nenhuma o que é uma
pessoa. Ao invés disso, o cânone menciona o Buda dizendo que definir a si
mesmo, de qualquer forma que seja, é limitar a si mesmo e que a pergunta, “O
que sou eu?” é melhor ser ignorada. Isso sugere que ele formulou o conceito dos
khandhas para responder outras questões distintas. Se, como meditadores,
queremos fazer o melhor uso desse conceito, deveríamos procurar saber quais
eram essas questões originais e determinar como elas se aplicam à nossa
prática.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O cânone retrata o Buda
dizendo que ele apenas ensinava dois tópicos: o sofrimento e o fim do
sofrimento. Uma análise dos discursos em Pali mostra ele empregando o conceito
dos khandhas para responder a questões básicas relacionadas com esses tópicos:
O que é sofrimento? Como é causado? O que pode ser feito para que essas causas
tenham fim? <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O Buda introduziu o
conceito dos khandhas no seu primeiro sermão em resposta à primeira dessas
questões. A sua definição resumida do sofrimento era “os cinco khandhas influenciados
pelo apego.” Essa frase críptica pode ser explicada recorrendo a outros trechos
do cânone. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os cinco khandhas são maços
ou amontoados de forma, sensações, percepções, formações e consciência. Nenhum
dos textos explica porque o Buda empregou a palavra khandha para descrever
essas coisas. O significado de “tronco de árvore” pode ser relevante para o
simbolismo do fogo, tão abundante no cânone – nibbana como a extinção dos fogos
da cobiça, raiva e delusão – mas nenhum dos textos faz essa conexão de modo
explícito. A imageria mais comum e explícita é dos khandhas como um incômodo.
Podemos imaginá-los como pilhas de tijolos que carregamos sobre os nossos
ombros. No entanto, essas pilhas são melhor compreendidas, não como objetos,
mas como atividades, pois um importante texto define-as em relação às suas
funções.<span style="mso-spacerun: yes">  </span>Forma – que abrange todos os
tipos de fenômenos físicos, dentro e fora do corpo -<span style="mso-spacerun:
yes">  </span>está sujeita ao desgaste ou à “desforma.” A sensação sente
prazer, dor e nem prazer, nem dor. A percepção rotula ou identifica os objetos
ou fenômenos. A consciência realiza a cognição através dos seis sentidos,
(contando a mente como sendo o sexto), juntamente com os seus respectivos objetos.
Dos cinco khandhas, as formações são o mais complexo. Trechos do cânone definem
as formações<span style="mso-spacerun: yes">  </span>como intenção, mas elas
também incluem uma gama variada de atividades, tais como a atenção, a avaliação
e todos os processos ativos da mente. Também é o khandha mais fundamental pois
a sua atividade principal é tomar o potencial das experiências da forma,
sensações, etc., que provêm do passado e transformá-las na experiência tangível
dessas coisas no momento presente.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, a intenção é parte
integrante da<span style="mso-spacerun: yes">  </span>nossa experiência de
todos os khandhas – um ponto importante, pois isso significa que existe um
elemento de intenção como parte de todo sofrimento. Isso abre a possibilidade
de ser possível dar fim ao sofrimento mudando as nossas intenções – ou
abandonando-as por completo – que é precisamente o propósito dos ensinamentos
do Buda. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Para compreender como isso
ocorre, temos que investigar um pouco mais de perto como o sofrimento surge,
ou, em outras palavras, como os khandhas se tornam os khandhas influenciados
pelo apego. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando os khandhas são
experimentados, o processo das formações em geral não pára simplesmente nisso.
Se a atenção focar nas características atrativas dos khandhas – formas belas,
sensações prazerosas, etc. – isso poderá dar origem à paixão e ao deleite. Essa
paixão e deleite podem assumir muitas formas, mas a mais tenaz é ação habitual
de fabricar um senso de eu ou meu, identificando-se com um khandha em
particular, (ou conjunto de khandhas), ou reivindicando a posse deste. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Essa noção de um eu e meu
raramente é imóvel. Ela vagueia como uma ameba, mudando de perfil à medida que
muda de localidade. Algumas vezes expandida, algumas vezes contraída, pode ver
a si mesma como idêntica ao khandha, como possuidora de um khandha, como
existindo dentro de um khandha, ou como tendo um khandha dentro de si. Em
certas ocasiões, sentindo-se finita, em outras, infinita, qualquer forma
assumida será sempre instável e insegura, pois os khandhas que proporcionam o
seu alimento são simplesmente atividades e funções, impermanentes e
insubstanciais. De acordo com as palavras do cânone, os khandhas são como a
espuma, como uma miragem, como as bolhas que se formam quando a chuva cai sobre
a água. Os khandhas são pesados só porque a empunhadura de ferro com a qual
tentamos agarrá-los é um incômodo. Enquanto estivermos viciados na paixão e no
deleite por essas atividades – enquanto nos apegarmos a elas, estaremos
destinados ao sofrimento. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A abordagem Budista para
dar fim a esse apego, no entanto, não é a de simplesmente abandoná-lo. Como
ocorre com qualquer vício, a mente tem que ser “desmamada” gradualmente. Antes
que possamos alcançar o ponto em que não há intenção, no qual estamos
totalmente libertos das fabricações dos khandhas, temos que mudar as nossas
intenções em relação aos khandhas para mudar as suas funções. Ao invés de
usá-los com o propósito de construir um eu, usamo-los com o propósito de criar
um caminho para o fim do sofrimento. Ao invés de carregarmos pilhas de tijolos
sobre os nossos ombros, nós<span style="mso-spacerun: yes">  </span>retiramos
essas pilhas dos nossos ombros e<span style="mso-spacerun: yes"> 
</span>colocamos tudo no chão como calçamento. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O primeiro passo neste
processo é usar os khandhas para construir os fatores do nobre caminho óctuplo.
Por exemplo, a concentração correta: nós mantemos a percepção estável focada
num aspecto da forma, como a respiração, e empregamos o pensamento aplicado e
sustentado – que são parte das formações – para criar sensações prazerosas e agradáveis
que se propagam pelo corpo. No início, é normal que experimentemos paixão e
deleite por essas sensações e que a consciência as acompanhe. Isso nos ajudará
a ficar totalmente ocupados em obter a maestria nas habilidades da
concentração. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Uma vez que tenhamos obtido
a noção de força e bem estar que provém da maestria dessas habilidades, podemos
prosseguir para o segundo passo: dar atenção às desvantagens, até mesmo, dos
khandhas refinados que experimentamos durante a concentração, para enfraquecer
a paixão e deleite que possamos sentir por eles:<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> “Suponha que um arqueiro ou aprendiz de arqueiro fosse praticar em um
boneco de palha ou monte de barro, de forma que após algum tempo ele fosse
capaz de atirar a<span style="mso-spacerun: yes">  </span>longas distâncias,
atirar com precisão uma série rápida, e de penetrar uma grande massa. Da mesma
forma, é o caso em que um bhikkhu ... entra e permanece no primeiro jhana ...
êxtase e prazer nascidos do afastamento. Ele observa qualquer fenômeno que
ocorra<span style="mso-spacerun: yes">  </span>conectado com o corpo,
sensações, percepções, formações e consciência, como impermanente, sofrimento,
uma enfermidade, um câncer, uma flecha, doloroso, uma aflição, estranho, uma
dissolução, um vazio. (De forma semelhante com os outros níveis de jhana.)” (<a
href="../sutta/ANIX.36.php">AN IX.36</a>)<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>As várias maneiras de
estimular o desapego também são khandhas, khandhas da percepção. Uma lista
padrão inclui as seguintes: percepção da impermanência,<span
style="mso-spacerun: yes">  </span>percepção do sofrimento, percepção do
não-eu,<span style="mso-spacerun: yes">  </span>percepção do repulsivo,<span
style="mso-spacerun: yes">  </span>percepção das desvantagens, percepção do desencantamento,
percepção do desapego. Uma das percepções mais importantes é aquela do não-eu.
Quando o Buda apresentou pela primeira vez, no seu segundo sermão, o conceito
de não-eu, ele também empregou um meio para fortalecer o impacto dele através
de uma série de questões baseadas nos khandhas. Tomando cada khandha, um após o
outro, ele perguntou: “Isso é permanente ou impermanente?” Impermanente. “</span>E
aquilo que é impermanente é sofrimento ou felicidade?<span lang=PT-BR
style='mso-ansi-language:PT-BR'>?” Sofrimento. “</span>E é adequado considerar
o que é impermanente, sofrimento, sujeito a mudanças como: ‘Isso é meu. Isso
sou eu. Isso é o meu eu’<span lang=PT-BR style='mso-ansi-language:PT-BR'>?”
Não. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Essas questões mostram o
papel complexo que os khandhas desempenham neste segundo passo do caminho.
Essas questões em si são khandhas – das formações – e elas empregam o conceito
dos khandhas para desmontar qualquer paixão e deleite que possa estar centrado
nos khandhas e criar sofrimento. Desse modo, nesta etapa, empregamos khandhas
que apontam para as desvantagens dos khandhas. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Se forem empregadas de
forma inábil, no entanto, essas percepções e formações podem simplesmente
substituir a paixão pelo seu oposto, a aversão. É por isso que elas têm que
estar baseadas no primeiro passo – o bem estar construído no jhana – e acoplado
ao terceiro passo, as percepções do desapego e cessação que inclinam a mente na
direção do imortal: “Isto é a paz, isto é o sublime, isto é, o silenciar de
todas as formações, o abandono de todas as aquisições, a destruição do desejo;
desapego; cessação; Nibbana.” Esses são, na verdade, os<span
style="mso-spacerun: yes">  </span>khandhas da percepção que direcionam a mente
para além de todos os khandhas.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os textos dizem que esse
processo de três passos pode conduzir a um dentre dois resultados. Se, depois
de enfraquecer a cobiça pelos khandhas, a mente contiver alguma cobiça residual
pela percepção do imortal, ela alcançará o terceiro nível da iluminação,
chamado de não retorno. Se, no entanto, a cobiça forem completamente
erradicados, todo e qualquer apego será abandonado completamente, as intenções
que fabricam os khandhas serão deixadas de lado e a mente estará totalmente
libertada. Os tijolos do calçamento se converterão numa pista de decolagem e a
mente decolará. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Para onde? Os autores dos
discursos relutam em dizer, até o ponto de descrever isso como um estado de
existência, não existência, nenhum dos dois ou ambos. Como indicado num dos
discursos, a liberdade que está além dos khandhas também se encontra além do
âmbito no qual a linguagem pode ser empregada de modo apropriado. Existe também
o problema real e prático de que qualquer noção preconcebida dessa liberdade,
se agarrada como um khandha da percepção, poderá facilmente funcionar como um
obstáculo para a sua realização. Por outro lado, existe também a possibilidade
de que, se<span style="mso-spacerun: yes">  </span>empregado de forma adequada,
esse tipo de khandha da percepção possa funcionar como uma ajuda no caminho.
Assim, os discursos oferecem pistas na forma de símiles, referindo-se à
libertação total como: <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>Não-formado, o fim,<br>
sem impurezas, a verdade, o que está mais além, <br>
o sutil, o difícil de ser visto,<br>
o que não envelhece, permanência, o que não se decompõe,<br>
o sem características, não-elaboração,<br>
paz, o imortal,<br>
sublime, felicidade, consolação,<br>
o esgotamento do desejo, <br>
o excelente, o maravilhoso,<br>
o seguro, segurança, <br>
desatamento,<br>
o sem aflições, o sem paixões, o puro,<br>
libertação, desapego,<br>
a ilha, abrigo, porto seguro, refúgio,<br>
&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp; o último. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Outros trechos mencionam a
consciência nessa liberdade – “sem características ou superfície, sem fim, totalmente
luminosa” – como além dos limites de tempo e espaço, experimentada quando as
seis esferas sensuais deixam de funcionar. Nesse aspecto ela difere do khandha
da consciência, que depende das seis esferas sensuais e que pode ser descrito
através de termos como próximo ou distante, passado, presente ou futuro. A
consciência sem características é dessa forma a consciência da iluminação. E a
liberdade dessa consciência é transportada mesmo quando uma pessoa iluminada
retorna para a consciência comum. Como o Buda disse de si mesmo: <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> “Liberto, desassociado e emancipado da forma, o <a name=PVW>Tathagata</a>
permanece com a consciência irrestrita, Liberto, desassociado e emancipado das
sensações … percepções … formações … consciência … nascimento … envelhecimento
… morte … sofrimento e estresse … impurezas, o Tathagata permanece com a
consciência irrestrita.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso mostra mais uma vez a
importância de formular as questões corretas no que diz respeito aos
ensinamentos sobre os khandhas. Se você empregá-los para definir aquilo que
você é como uma pessoa, irá se restringir sem nenhuma razão. As questões
continuarão se acumulando. Mas se você empregá-los para dar um fim ao
sofrimento, as suas questões serão descartadas e você estará livre. Você nunca
mais se apegará aos khandhas e não precisará mais usá-los para dar fim ao
sofrimento criado por você mesmo. Enquanto você ainda estiver vivo, poderá
empregar os khandhas, quando necessário, para qualquer fim hábil que você
considerar adequado. Depois disso, você estará libertado de todos os usos e
necessidades, incluindo a necessidade de encontrar palavras para descrever essa
liberdade para si mesmo ou para qualquer outra pessoa.<o:p></o:p></span></p>

