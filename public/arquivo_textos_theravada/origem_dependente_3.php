<p class=Tit1 align=center style='text-align:center'>3. O Homem e a Natureza</Tit1>




<p class=Normal>&nbsp;</p>

<hr size=2 width="100%" align=center>

<p class=Normal>&nbsp;</p>



<p class=Normal>Todos organismos vivos são compostos dos cinco <i>khandhas</i>
(grupos): <i>rupa</i> ou forma material; <i>vedana</i>, sensação; <i>sañña</i>,
percepção; <i>sankhara</i>, formações ou impulsos volitivos; e <i>viññana</i>,
consciência. Não existe um dono ou diretor dos <i>khandhas</i>, quer seja como
parte deles ou no seu exterior. Em qualquer investigação da vida, os cinco <i>khandhas</i>
são uma base suficientemente abrangente para realizar o trabalho. Os cinco <i>khandhas</i>
prosseguem em conformidade com o princípio da Origem Dependente, existindo como
parte do contínuo de fatores determinados inter-relacionados e interdependentes.</p>

<p class=Normal>Nesse contexto, os cinco <i>khandhas</i>, ou vida, estão
sujeitos às Três Características: eles se encontram numa condição de <i>aniccata</i>
– impermanentes e instáveis; <i>anattata</i> – não possuem um eu ou essência
intrínseca; e <i>dukkhata</i> – sendo constantemente oprimidos pela origem
e cessação e prontos a causar o sofrimento sempre que com eles nos associemos
por meio da ignorância. Os cinco <i>khandhas</i>, prosseguindo dessa forma com
mudanças constantes e livre de qualquer entidade intrínseca, estão sujeitos
apenas ao fluxo contínuo natural dos fatores determinantes inter-relacionados.
Mas para a maioria das pessoas, a resistência ao fluxo resulta do apego
equivocado a uma ou outra característica desse contínuo como sendo o eu e do
desejo que esse eu prossiga de um certo modo específico. Quando as coisas não
obedecem aos desejos, o estresse resultante causa frustração e posteriormente
um apego ainda mais intenso. A vaga noção da inevitabilidade da mudança nesse
eu tão querido, ou a suspeita de que ele possa na verdade não existir, fazem
com que esse apego e desejo se tornem ainda mais desesperados, e o medo e a
ansiedade fincam raízes profundas na mente.</p>


<p class=Normal>Os estados mentais são <i>avijja</i> – a ignorância da
verdade, vendo as coisas como sendo o eu; <i>tanha</i> – desejo de que esse eu
imaginário obtenha várias coisas ou estados; e <i>upadana</i> - apego e
adesão a essas idéias equivocadas e tudo aquilo que elas implicam. Essas
contaminações estão arraigadas na mente, de onde elas dirigem o nosso
comportamento, moldam a identidade e influenciam as mudanças de curso nas
nossas vidas, tanto de modo explícito como velado. Em geral, essas são as
causas do sofrimento para todos seres não iluminados.</p>



<p class=Normal>Em essência estamos lidando com a desarmonia entre dois
processos:</p>


<p class=Normal>1. <b>O processo natural da vida, </b>que prossegue<b> </b>de
acordo com a lei fixa, natural, das três Características.<span
style="mso-spacerun: yes">  </span>Estas são expressas através do nascimento (<i>jati</i>),
envelhecimento (<i>jara</i>)<span style="mso-spacerun: yes">  </span>e morte (<i>marana</i>),
tanto no seu sentido mais básico como no mais profundo.</p>



<p class=Normal>2. <b>O processo fabricado do desejo e apego, </b>baseado na
ignorância da verdadeira natureza da vida, que provoca a percepção equivocada
de, e um apego a, um eu – ‘criando um eu por meio do qual obstruir o fluxo da
natureza.’ Essa é uma vida limitada pela ignorância, vivida com apego, em
cativeiro, em contradição com a lei da Natureza e vivida com medo e
sofrimento.</p>



<p class=Normal>A vida, sob o ponto de vista da ética, pode-se dizer que
compreende dois tipos de eu. Qualquer contínuo vital em particular,
prosseguindo de acordo como o seu curso natural condicionado, embora despojado
de uma essência duradoura, pode apesar disso ser identificado como um contínuo
distinto dos demais. Isso é chamado de ‘eu convencional’ e essa convenção pode
ser empregada de forma hábil em relação à conduta moral.</p>



<p class=Normal>Depois existe o eu ‘fabricado,’ produzido pela ignorância e
agarrado com força pelo desejo e apego. O eu convencional não é fonte de
problemas quando é entendido como tal, de forma clara. O eu ‘fabricado,’ no
entanto, oculto dentro do eu convencional, é o eu do apego, que tem que se
sujeitar às vicissitudes do primeiro eu e por isso produz o sofrimento. Em
outras palavras, é um processo em dois níveis: num nível está o eu
convencional, no outro nível está o apego deludido ao eu convencional, como se
este fosse uma realidade absoluta. Se o apego deludido se transformar em
compreensão e entendimento, o problema será solucionado.</p>



<p class=Normal>Um modo de vida fundamentado no apego à noção de um eu
implanta medo e ansiedade de forma profunda na psique, de onde estes controlam
o comportamento e escravizam a pessoa mundana inocente. Uma perspectiva de vida
baseada no apego ao conceito de um eu traz muitas repercussões prejudiciais,
tais como:</p>



<p class=Normal style='margin-left:.75in'>Apego
     a desejos egoístas, (<i>kamupadana</i>), a busca interminável pela
     satisfação destes e a sede ambiciosa pelos objetos do desejo;</p>
<p class=Normal style='margin-left:.75in'>A
     identificação e a aderência inquebrantável às idéias <i>(ditthupadana</i>),
     avaliando-as como sendo o eu ou como pertencendo ao eu. É como construir
     uma parede que obstrua a verdade ou mesmo fugir dela de forma completa.
     Esse tipo de apego produz uma deficiência na fluidez dos poderes de
     raciocínio e conduz à arrogância e intolerância;</p>
<p class=Normal style='margin-left:.75in'>Apego
     a preceitos e rituais (<i>silabbatupadana</i>). Percebendo apenas uma
     relação mística ou tênue em tais práticas, não se pode nunca estar
     verdadeiramente seguro delas, mas o medo e a preocupação com o eu
     fabricado produzem um esforço desesperado para se agarrar a qualquer coisa
     que possa servir como fonte de segurança, não importando quão mística ou
     obscura esta seja;</p>
<p class=Normal style='margin-left:.75in'>A
     noção de um eu independente, (<i>attavadupadana</i>), que é firmemente
     agarrada, mantida e protegida do dano ou destruição. O sofrimento então
     surge como resultado das inquietações postas sobre esse ‘eu do apego’
     oprimido.</p>




<p class=Normal>Nesse contexto, o estresse e sofrimento surgem não somente
no indivíduo, mas também radiam para o exterior, para toda a sociedade. Assim,
a condição do apego (<i>upadana</i>) pode ser identificada como a fonte
principal de todos os problemas gerados pelo homem na sociedade.</p>



<p class=Normal>O ciclo de Origem Dependente mostra a origem dessa vida
estressante, auto-centrada, e o seu inevitável resultar em sofrimento. Com o
rompimento do ciclo, a vida estressante é transformada completamente,
resultando numa vida vivida com sabedoria, em harmonia com a natureza e
libertada do apego a um eu.</p>



<p class=Normal>Viver com sabedoria significa viver com a plena consciência
de como as coisas são e de saber como se beneficiar da natureza; beneficiar-se
da natureza significa viver em harmonia com a natureza; viver em harmonia com a
natureza é viver com liberdade; viver com liberdade é estar livre do poder do
desejo e do apego; viver sem apego significa viver com sabedoria, entender e
relacionar-se<span style="mso-spacerun: yes">  </span>com as coisas por meio da
compreensão do processo de causa e efeito.</p>



<p class=Normal>De acordo com os ensinamentos do Buda, não existe nada que
exista além ou separado da natureza, quer seja como um poder místico
controlando os eventos do exterior, ou relacionado ou envolvido de alguma outra
forma nas ocorrências da natureza. Tudo aquilo que esteja associado com a
natureza não pode ser separado da natureza, mas tem que ser um componente dela.
Todos os eventos na natureza prosseguem de acordo com a orientação da
inter-relação dos fenômenos naturais. Não existem acidentes, nem existe uma
força criativa independente de causas. Eventos aparentemente impressionantes e
miraculosos surgem inteiramente a partir de causas, mas como algumas vezes as
causas estão obscurecidas do nosso conhecimento, esse eventos podem parecer
miraculosos. No entanto, qualquer noção de perplexidade ou assombro desaparece
com rapidez uma vez que a causa de tais eventos seja compreendida. A palavra
‘sobrenatural’ é simplesmente um artifício de linguagem que se refere ao
que excede a nossa compreensão naquele momento, mas na verdade não existe nada
que seja verdadeiramente ‘sobrenatural’.</p>



<p class=Normal>O mesmo se aplica à nossa relação com a natureza. O modo
de falar, que descreve os seres humanos como separados da natureza ou
controlando a natureza, é simplesmente um artifício de linguagem. Os seres
humanos são parte da natureza, não estão separados dela. Dizer que controlamos
a natureza simplesmente quer dizer que nos tornamos fatores determinantes
dentro do processo de causa e efeito. O elemento humano contém fatores mentais,
incluindo a intenção, que estão envolvidos no processo de ação e resultado que
juntos são conhecidos como ‘criação.’ No entanto, a humanidade não é capaz de
criar algo a partir do nada, independente das causas naturais. Nosso assim
chamado controle da natureza surge da nossa habilidade em reconhecer os fatores
requeridos para produzir um resultado em particular e de saber como
manipulá-los.</p>



<p class=Normal>Existem dois estágios nesse processo. O primeiro é o
conhecimento que leva ao segundo estágio, tornando-se um catalisador para os
outros fatores. Desses dois estágios, o conhecimento é o crucial. Através desse
conhecimento, o homem é capaz de utilizar e participar no processo de causa e
efeito. Somente ao interagir e influenciar as coisas de modo sábio pode-se
dizer que o homem ‘controla a natureza.’ Neste caso, o conhecimento humano, as
habilidades e as ações se tornam fatores adicionais dentro do processo natural.</p>



<p class=Normal>Este princípio se aplica tanto aos fenômenos físicos como aos
mentais. A afirmação, ‘beneficiar-se da natureza é também viver em harmonia com
a natureza’, está baseada na realidade da natureza interdependente de ambos os
fenômenos, físicos e mentais. Poderíamos dizer igualmente, ‘controlando os
aspectos mentais da natureza’ ou ‘controlando a mente’ e isso também seria
válido. A sabedoria em relação tanto aos fenômenos físicos como aos mentais é
essencial para que realmente se possa beneficiar da natureza.</p>



<p class=Normal>Uma vida com sabedoria pode ser encarada sob duas perspectivas:
<p class=Normal style='margin-left:.75in'>Interiormente, é caracterizada pela serenidade, alegria, atenção e liberdade.
Ao experimentar uma sensação agradável a mente não é embriagada ou deludida por
ela. Quando privada de confortos, a mente permanece firme, inabalável e
imperturbável. A felicidade e o sofrimento não estão mais empossados em objetos
externos.</p>



<p class=Normal style='margin-left:.75in'>O nível externo é caracterizado pela fluência, eficiência,
flexibilidade e liberdade de complexos incômodos e delusões.</p>



<p class=Normal>A seguir encontra-se um ensinamento do Buda que ilustra as
diferenças entre a vida vivida com apego e a vida vivida com sabedoria:</p>



<p class=Normal style='margin-left:.75in'>“Bhikkhus, uma pessoa comum sem
instrução sente sensações prazerosas, sente sensações dolorosas, sente
sensações nem dolorosas, nem prazerosas. Um nobre discípulo bem instruído
também sente sensações prazerosas, sente sensações dolorosas, sente sensações nem
dolorosas, nem prazerosas. Então, bhikkhus, qual é a variação, qual é a
distinção, qual é a diferença que distingue o<span style="mso-spacerun: yes"> 
</span>nobre discípulo bem instruído de uma pessoa comum sem instrução?”<o:p></o:p></p>


<p class=Normal style='margin-left:.75in'>“Bhikkhus, quando a pessoa comum
sem instrução é tocada por uma sensação dolorosa, ela<span style="mso-spacerun:
yes">  </span>fica triste, angustiada e lamenta, bate no peito, chora e fica
perturbada. Dessa maneira, ela sente duas dores, corporal e mental. Como se ela
fosse atingida por uma flecha e, logo em seguida, fosse atingida por outra
flecha, de modo que ela sentiria a sensação de dor de duas flechas. Da mesma
forma, a pessoa comum sem instrução é tocada por uma sensação dolorosa, ela <span
style="mso-spacerun: yes"> </span>fica triste, angustiada e lamenta, bate no
peito, chora e fica perturbada. Dessa maneira ela sente duas dores, corporal e
mental.<o:p></o:p></p>


<p class=Normal style='margin-left:.75in'>“Ao ser tocada por essa mesma
sensação dolorosa, ela sente aversão pela sensação de dor. Sentindo aversão
pela sensação dolorosa, a tendência subjacente à aversão é aquilo que está por
detrás disso. Ao ser tocada pela sensação dolorosa, ela busca prazer nos
prazeres sensuais. Por qual razão? Porque a pessoa comum sem instrução não sabe
como escapar das sensações dolorosas, exceto através dos prazeres sensuais. Quando ela busca prazer nos prazeres sensuais, a tendência subjacente ao desejo
sensual é aquilo que está por detrás disso. Ela não compreende como na verdade
é a origem e a cessação, a gratificação, o perigo e a escapatória dessas
sensações. Quando ela não compreende essas coisas, a tendência subjacente à
ignorância em relação à sensação nem dolorosa, nem prazerosa é aquilo que está
por detrás disso. <o:p></o:p></p>


<p class=Normal style='margin-left:.75in'>“Se ela sentir uma sensação
prazerosa, ela sente isso com apego. Se ela sentir uma sensação dolorosa, ela
sente isso com apego. Se ela sentir uma sensação nem dolorosa, nem prazerosa,
ela sente isso com apego. Essa, bhikkhus, é uma pessoa comum sem instrução que
está apegada ao nascimento, envelhecimento, morte, tristeza, lamentação, dor,
angústia e desespero; ela está apegada ao sofrimento, eu digo.<o:p></o:p></p>


<p class=Normal style='margin-left:.75in'>“Bhikkhus, quando um nobre
discípulo bem instruído é tocado por uma sensação dolorosa, ele não<span
style="mso-spacerun: yes">  </span>fica triste, angustiado e lamenta, não bate
no peito, chora e fica perturbado. Ele sente apenas uma sensação - corporal,
não a sensação mental. Como se ele fosse atingido por uma flecha e, não fosse
atingido por outra flecha, de modo que ele sentiria a sensação de dor de
uma<span style="mso-spacerun: yes">  </span>flecha só. Da mesma forma, um nobre
discípulo bem instruído é tocado por uma sensação dolorosa, ele não<span
style="mso-spacerun: yes">  </span>fica triste, angustiado e lamenta, não bate
no peito, chora e fica perturbado. Ele sente apenas uma sensação - corporal,
não a sensação mental. <o:p></o:p></p>


<p class=Normal style='margin-left:.75in'>“Ao ser tocado por essa mesma
sensação dolorosa, ele não sente aversão pela sensação de dor, a tendência
subjacente à aversão não está por detrás disso. Ao ser tocado pela sensação
dolorosa, ele não busca prazer nos prazeres sensuais. Por qual razão? Porque o
nobre discípulo bem instruído sabe como escapar das sensações dolorosas de
outro modo que através dos prazeres sensuais. Visto que ele não busca prazer
nos prazeres sensuais, a tendência subjacente ao desejo sensual não está por
detrás disso. Ele compreende como na verdade é a origem e a cessação, a
gratificação, o perigo e a escapatória dessas sensações. Visto que ele
compreende essas coisas, a tendência subjacente à ignorância em relação à
sensação nem dolorosa, nem prazerosa não está por detrás disso. <o:p></o:p></p>


<p class=Normal style='margin-left:.75in'>“Se ele sentir uma sensação
prazerosa, ele sente isso desapegado. Se ele sentir uma sensação dolorosa, ele
sente isso desapegado. Se ele sentir uma sensação nem dolorosa, nem prazerosa,
ele sente isso desapegado. Esse, bhikkhus, é um nobre discípulo bem instruído
que está desapegado do nascimento, envelhecimento, morte, tristeza, lamentação,
dor, angústia e desespero; ele está desapegado do sofrimento, eu digo.<o:p></o:p></p>


<p class=Normal style='margin-left:.75in'>“Essa, bhikkhus, é a distinção, a
disparidade, a diferença entre o nobre discípulo bem instruído e a pessoa comum
sem instrução.”&nbsp;<span lang=PT-BR style='mso-ansi-language:PT-BR'>[SN.IV.207-210]
(<a href="../sutta/SNXXXVI.6.php">SNXXXVI.6</a>)<o:p></o:p></span></p>

