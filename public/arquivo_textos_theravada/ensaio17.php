<p class=Normal>A repentina entrada em
circulação generalizada de um termo bem conhecido com uma nova conotação, com
freqüência tem um significado que vai além da mera curiosidade filológica.
Visto que a linguagem é moldada pelo pensamento em um nível anterior e mais
básico do que aquele de um modelo deliberado, tais mudanças nos usos
lingüísticos podem muito bem sinalizar mudanças mais profundas que ocorrem no
composto mental daqueles que fazem uso do termo. Elas podem ser vistas como
indicadores barométricos de transformações na esfera da consciência - nos
nossos padrões de pensamento, nas nossas atitudes, nos nossos objetivos. <o:p></o:p></span></p>

<p class=Normal>Se existe um termo que
poderia ser escolhido para caracterizar o clima intelectual e moral dos dias de
hoje, esse termo seria &quot;liberalidade&quot;. Essa palavra aparentemente
incolor veio para marcar a conclusão do embate centenário contra o peso
opressivo das tradições estabelecidas em tantas áreas diversas do interesse humano.
As suas seis sílabas são um hino de vitória, para o triunfo do método empírico
sobre o dogma formulado como a chave para o conhecimento, para a primazia da
consciência individual sobre a moralidade prescrita no domínio da ética e nas
nossas vidas particulares, para a substituição do reino do superego por uma
liberdade recém-descoberta<span style="mso-spacerun: yes">  </span>para
explorar os canais subterrâneos do impulso e desejo em qualquer direção que
eles possam levar. <o:p></o:p></span></p>

<p class=Normal>Talvez, o mais importante é
que a noção de liberalidade também aponte para uma atitude particular em
relação à experiência, uma atitude que silenciosamente tem permeado a nossa
cultura de forma tão completa que agora até parece uma característica inata do
temperamento humano. De forma sucinta essa atitude pode ser descrita como a
afirmativa suave e afável da experiência na sua totalidade, combinada com uma
receptividade flexível a todas as suas possíveis formas. Essa atitude, devemos
enfatizar, só raramente se solidifica em uma convicção consciente; normalmente
ela está presente no fundo da mente como uma intuição não verbalizada, uma
orientação em relação ao mundo, fluída e passível de mudanças. Tendo,
historicamente, as suas raízes no declínio generalizado dos sistemas de
crenças, centrados em um objetivo transcendente para a vida humana e em uma
escala objetiva de valores, a filosofia da liberalidade toma toda verdade como
sendo relativa, todos os valores como pessoais e subjetivos. Assim, ela defende
que a nossa tarefa na vida é estarmos abertos, tanto quanto possível, para as
revelações do milagre da existência e de celebrar as suas infinitas
possibilidades. <o:p></o:p></span></p>

<p class=Normal>A disseminação dessa
atitude através da cultura em geral também deixou a sua marca nas atuais
interpretações do Budismo. Nós, então, notamos que para muitos dos professores
Budistas contemporâneos o Dhamma é essencialmente um método para obter a
consumação de tudo aquilo que a noção de liberalidade implica. Sob essa
perspectiva o Budismo não é uma doutrina com um distinto conjunto de
princípios, não é uma disciplina que nos guia para um objetivo supramundano,
mas um instrumento para a abertura para o aqui e agora. A deficiência mais
fundamental que forma a base do sofrimento humano, acredita-se, é a nossa
tendência de nos isolarmos das experiências, de nos fecharmos dentro dos nossos
conceitos e julgamentos em um compartimento limitado da realidade.
Desenvolvendo através da meditação uma consciência &quot;não seletiva&quot;,
que não discrimina e que permite a qualquer coisa que surja que ocupe o seu
espaço, estamos criando as condições para romper os nossos constrangimentos e
nos fundirmos com a torrente dos acontecimentos, de dançar com as &quot;dez mil
coisas&quot; - aceitando-as todas sem no entanto nos apegarmos a elas. <o:p></o:p></span></p>

<p class=Normal>Enquanto os defensores da
liberalidade são, em geral, hábeis em assimilar os seus princípios ao Dhamma
clássico, um exame cuidadoso revela diferenças substanciais entre ambos. Aqui,
quero focar somente em algumas diferenças cruciais na sua respectiva orientação
em relação à experiência. Deve ser notado de imediato que, enquanto que a
escola da liberalidade nos convida a deixar de lado nossas discriminações,
julgamentos e autocontrole para que possamos imergir no fluxo dinâmico da
experiência imediata, o Buda prescreve uma atitude em relação à experiência que
derive de um julgamento moldado com cuidado, que empregue discernimento preciso
e que resulte em desapego e autocontrole. Essa atitude, a contrapartida budista
clássica<span style="mso-spacerun: yes">  </span>para o moderno programa de
liberalidade pode ser resumida em uma palavra encontrada em todos lugares nos
textos mais antigos. Essa palavra é aplicação <i>(appamada).</i> <o:p></o:p></span></p>

<p class=Normal>Aplicação denota uma
atitude de escrutínio crítico dirigido à própria mente, tanto em relação aos
seus movimentos internos, como às suas reações aos assuntos externos. O termo
sugere esforço diligente e severa atenção, e além disso soa uma nota de cuidado
e cautela moral. Assim, a palavra implica, como era a intenção do Buda indicar,
que estamos constantemente expostos ao perigo - um perigo que surge de dentro e
que se torna ainda mais iminente na medida em que permitirmos que a aplicação
escape e escorreguemos para o seu oposto: a negligência <i>(<a name=PVW>pamada</a>).</i>
<o:p></o:p></span></p>

<p class=Normal>Tal cautela é necessária porque as ações possuem conseqüências que se
estendem além delas mesmas. Enquanto que a escola da liberalidade tende a
subordinar a preocupação com as conseqüências das ações à ênfase sobre a
permanência no momento presente, o Dhamma clássico ensinado pelo Buda nos pede
para reconhecer que todas as ações volitivas, mesmo os nossos pensamentos e
impulsos mais fugazes, são sementes com raízes enterradas no fundo da mente em
um passado cujo princípio não pode ser definido, e com o potencial de gerar
resultados nos horizontes distantes do futuro. Essas conseqüências das ações a
longo prazo são de enorme importância para nós; pois não importa quão distantes
da nossa visão elas possam estar agora, quando chegar a hora das nossas ações
gerarem os seus frutos, nós mesmos é que teremos de experimentá-las. Como esses
frutos são invariavelmente determinados pela qualidade moral das ações, o<span
style="mso-spacerun: yes">  </span>exame diligente de si mesmo - isto é, a
circunspecção - é necessário e urgente para que possamos refrear-nos de
praticar aquelas ações que parecem agradáveis mas que geram resultados
desagradáveis, e assim possamos nos aplicar naquelas ações que podem ser
difíceis mas que produzem benefícios a longo prazo. <o:p></o:p></span></p>

<p class=Normal>A forma de pensar baseada
na liberalidade rejeita a dualidade, como um produto da discriminação e dos
conceitos deludidos. Ela tacitamente pressupõe que a existência como tal é no
final das contas benigna; que mais além dos nossos conceitos deludidos, a rica
e vívida diversidade de formas possuem um único sabor, um sabor que é doce. Em
contraste, a atitude de se aplicar está baseada na idéia de que a textura da
existência é completamente composta por dualidades que são profundas e
inevitavelmente reais. O mundo é testemunha dessa visão no contraste entre a
superfície sedutora e deliciosa das coisas e o seu vazio e imperfeição
subjacentes; as nossas mentes são testemunhas da competição contínua entre os
fatores mentais benéficos e os prejudiciais, entre o impulso ascendente pela
purificação e o puxão descendente das contaminações. O fato dessa dualidade não
ser banal pode ser visto pelas conseqüências: uma conduz a Nibbana, o estado de
libertação, o Imortal, enquanto que a outra conduz de regresso ao ciclo de
repetidos nascimentos, samsara, que também é o reino de Mara, o Senhor da
Morte. <o:p></o:p></span></p>

<p class=Normal>Aplicar-se é considerar
essas dualidades plenamente, com as suas profundas implicações. A pessoa que se
aplica não tem como objetivo uma consciência não seletiva, aberta para a
existência na sua totalidade, pois abrir-se dessa forma é arriscar tornar-se
vulnerável, justamente, aos elementos dentro de si mesmo que mantêm a pessoa
presa ao reino de Mara. A consciência desenvolvida através da aplicação é
construída sobre uma escolha - uma escolha cuidadosa de abandonar aquelas
qualidades que a pessoa entende serem prejudiciais, e de desenvolver no seu
lugar aquelas qualidades que a pessoa entende serem benéficas, os estados que
conduzem à pureza e à paz. <o:p></o:p></span></p>

<p class=Normal>Tanto nas nossas conexões
com o mundo como no cortejo íntimo de pensamentos, imaginações e emoções dentro
da mente, continuamente nos deparamos com uma bifurcação no caminho. Um ramo
dessa bifurcação acena com a promessa de prazer e satisfação, mas ao final
conduz à dor e ao cativeiro; o outro, escarpado e difícil de subir, conduz para
cima para a iluminação e libertação. Jogar fora o discernimento e o julgamento
em troca de uma liberalidade relaxada com relação ao mundo é nublar a
importante distinção entre esses dois caminhos bastante diferentes. Ser
aplicado é ter consciência da dicotomia e de fazer o esforço para evitar um e
perseverar no outro. Como nos faz lembrar o Buda, a aplicação é o caminho para
o Imortal, a negligência é o caminho da Morte. <o:p></o:p></span></p>


