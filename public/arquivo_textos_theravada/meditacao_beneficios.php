<p class=Normal>Alcançar o estado de atenção plena através da meditação tem ajudado as pessoas a manterem uma mente saudável ensinando-lhes como enfrentar as emoções e os pensamentos negativos, como por exemplo o controle da cobiça, da raiva e da ansiedade, e o incentivo de disposições mais positivas, como empatia, compaixão e perdão. Aqueles que já colheram os benefícios da atenção plena sabem que ela funciona. Mas como exatamente a meditação funciona?</p>

<p class=Normal>Pesquisadores do Hospital Brigham and Women (BWH) da Universidade de Harvard, propuseram um novo modelo que muda a forma como pensamos sobre a atenção plena. Ao invés de descrever a atenção plena como uma única dimensão da cognição, os pesquisadores propõem que a atenção plena na verdade envolve um amplo quadro de mecanismos complexos no cérebro.</p>

<p class=Normal>Em essência, eles delinearam a ciência por trás da atenção plena.</p>

<p class=Normal>Este novo modelo de atenção plena foi publicado em 25 de Outubro de 2012 na revista científica Frontiers in Human Neuroscience.</p>

<p class=Normal>Os pesquisadores identificaram várias funções cognitivas que ficam ativas no cérebro durante a prática da meditação da atenção plena. Essas funções cognitivas ajudam o praticante a desenvolver a auto-consciência, o auto-controle e a auto-transcendência, que compõem o quadro transformativo do processo de atenção plena.</p>

<p class=Normal>Os cientistas batizaram sua teoria de S-ART, pelas iniciais em inglês das três funções cognitivas envolvidas - Self-Awareness, Self-Regulation, e Self-Transcendence.</p>

<p class=Normal>O modelo S-ART explica os mecanismos neurobiológicos subjacentes através dos quais a meditação da atenção plena pode facilitar a auto-consciência; reduzir os preconceitos e os pensamentos negativos; melhorar a capacidade de controlar o próprio comportamento; e aumentar as relações pró-sociais positivas consigo mesmo e com os outros - enfim, criando uma mente saudável sustentável.</p>

<p class=Normal>Os pesquisadores destacam seis processos neuropsicológicos que são mecanismos ativos no cérebro durante a prática da meditação da atenção plena e que dão suporte à S-ART.</p>

<p class=Normal>Estes processos incluem: 1) intenção e motivação; 2) controle da atenção; 3) controle emocional; 4) extinção e reconsolidação; 5) comportamento pró-social; e 6) desapego e descentralização.</p>

<p class=Normal>Em outras palavras, o desenvolvimento da atenção plena começa com a intenção e motivação em querer atingir a atenção plena, que opere por si mesma e menos focada em resultados, seguida por maior consciência dos hábitos da pessoa (bons e ruins). Uma vez que estes tenham sido identificados, a pessoa pode começar a domar a si mesma para se tornar menos reativa emocionalmente e para se recuperar mais rapidamente de emoções perturbadoras.</p>

<p class=Normal>"Através da prática contínua, a pessoa pode desenvolver um distanciamento psicológico de todos os pensamentos negativos e pode inibir os impulsos naturais que constantemente alimentam os maus hábitos," disse David Vago, PhD, BWH Functional Neuroimaging Laboratory, Department of Psychiatry, líder do estudo.</p>

<p class=Normal>O Dr. Vago afirma que a prática continuada da meditação também pode aumentar a empatia e eliminar nossos apegos às coisas de que gostamos, e as aversões às coisas de que não gostamos.</p>

<p class=Normal>"O resultado da prática é um novo Você, com um novo conjunto de habilidades multidimensionais para reduzir os preconceitos que cercam a própria experiência interna e externa, e para manter uma mente saudável," disse Vago.</p>

