<p class=Normal>Embora a impermanência, dukkha e não-eu sejam características comuns a todas as coisas e se revelem constantemente, as pessoas em geral não as percebem. Elas ficam obscurecidas. Se uma pessoa não prestar atenção e investigar do modo correto, ela não irá reconhecer os fatores que causam o obscurecimento. Esses fatores incluem: </p>

 

<p class=Normal style='margin-left:.75in'>

1. Continuidade (<i>Santati</i>): Oculta a impermanência<br>

2. Movimento (<i>Iriyapatha</i>): Oculta Dukkha<br>

3. Solidez (<i>Ghana</i>): Oculta não-eu</p>

 

<p class=Normal>Ao fracassar em dar cuidadosa atenção ao surgimento e cessação, ao nascimento e decadência, a pessoa permite que a continuidade obscureça a característica da impermanência. Todas as coisas que experimentamos continuamente surgem e desaparecem, mas esse surgimento e cessação ocorrem de um modo contínuo e veloz. Essa rápida sucessão engana as pessoas a verem as coisas como se fossem estáveis e imutáveis. Por exemplo, a nossa imagem, ou de um amigo, aparenta ser a mesma depois de passados alguns instantes, mas na medida em que mais tempo passa, a pessoa se dá conta que ocorreu uma mudança. No entanto, na verdade, a mudança ocorre incessantemente, sem qualquer intervalo visível. Um exemplo dessa distorção é quando alguém percebe um hélice girando em alta velocidade, que parece ser um disco imóvel. Quando a velocidade de rotação diminui é possível ver o hélice com suas várias lâminas girando. De modo semelhante quando alguém rapidamente faz um movimento circular com uma tocha com fogo, a impressão é de um círculo iluminado. Outro exemplo é a luz emitida por uma lâmpada, que é vista como uma esfera imóvel e brilhante, mas que na realidade é o resultado de uma corrente elétrica que oscila com rapidez. Quando alguém emprega os meios apropriados, prestando cuidadosa atenção ao surgimento e cessação das coisas, então a impermanência fica clara. </p>

 

<p class=Normal>Do mesmo modo, com a falta de atenção à pressão contínua, o movimento obscurece a característica de <i>dukkha</i>. As pessoas normalmente precisam de um certo tempo para perceber a instabilidade, que é a inabilidade de um objeto de manter ou sustentar a sua forma  original devido ao estresse e fricção das suas partes componentes. Se no ínterim o objeto é movido ou modificado, ou o observador é separado do objeto, a pressão e a tensão não são evidentes. A nossa experiência das coisas em geral acontece no contexto de movimentação e dessa forma <i>dukkha</i> não é reconhecido. Tomemos como exemplo o corpo humano. A pessoa não precisa esperar até que o corpo faleça; mesmo na vida diária a tensão sempre está presente  no corpo, evitando que a pessoa permaneça quieta no mesmo lugar numa posição em particular. Se alguém precisa ficar numa mesma posição durante muito tempo, quer seja em pé, sentado, caminhando ou deitado, a tensão no corpo vai aumentando gradativamente até o ponto da dor e da exaustão, tornando-se insuportável. A pessoa então precisa se movimentar, ou mudar de postura. Uma vez que a pressão (uma conseqüência do sinal de <i>dukkha</i>) cessa no corpo, a sensação de dor também cessa. (Quando uma sensação de dor desaparece, em geral surge no seu lugar uma sensação de relaxamento, que chamamos de 'felicidade'. Mas isso é simplesmente uma sensação. Na realidade apenas há uma atenuação e ausência de <i>dukkha</i> - pressão). Na vida diária, permanecer durante muito tempo numa única posição dói e a pessoa se apressa em mudar de posição. Normalmente as pessoas se mexem constantemente para evitar a sensação de desconforto. Esquivando-se do desconforto, <i>dukkha</i>, a verdade inerente a todas as condições é como conseqüência, ignorada. </p>

 

<p class=Normal>De modo semelhante, ao não dividir um objeto nos seus vários componentes, a característica de não-eu é obscurecida pela solidez: algo existindo como um bloco, uma massa, uma unidade amalgamada. Todas as coisas condicionadas são criadas pela união de elementos componentes. Uma vez que os elementos tenham sido separados, aquela unidade integrada que recebe um nome específico, não mais existe. Em geral os seres humanos não discernem essa verdade, sendo obscurecidos pela percepção de solidez: o reconhecimento ou denotação de algo como uma entidade consolidada. Isto é consistente com o ditado popular na Tailândia: 'A pessoa vê o casaco, mas não o tecido; a pessoa vê a boneca mas não o plástico.' As pessoas podem ser enganadas pela imagem de um casaco, deixando de notar o tecido com o qual ele foi feito. Na verdade não existe um casaco; somente existem inúmeros fios entrelaçados de acordo com determinado padrão. Se os fios forem soltos o tecido deixa de existir. Do mesmo modo, uma criança que apenas vê a boneca está deludida pela imagem; o plástico que é a verdadeira substância da boneca não é reconhecido. Se alguém discernir a verdade então haverá apenas plástico; nenhuma boneca será encontrada. Mesmo o plástico tem origem na sucessiva formação de elementos componentes. A percepção de solidez obscurece a característica de não-eu do modo  descrito nestes simples exemplos. Se alguém separar e analisar os componentes, a natureza do não-eu se torna clara. A pessoa vê as coisas como <i>anatta</i>.</p>

 

