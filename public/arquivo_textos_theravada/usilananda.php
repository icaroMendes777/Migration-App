<p class=Tit1 align=center style='text-align:center'>Sayadaw U Silananda</Tit1>

<p class=Normal>O Venerável U Silananda nasceu em Mandalay, Burma e se tornou um noviço em 1943; quatro anos depois ele recebeu a ordenação como bhikkhu. Um acadêmico por natureza ele foi aprovado em todos os três exames de Pali do governo Birmanês. Ele obteve duas graduações de Dhammacariya (Mestre do Dhamma) e foi professor na Arithokdayone Pali University e no Departamento de Estudos Orientais da University of Arts and Sciences em Myanmar (antiga Birmânia). U Silananda compilou o extensivo Dicionário Pali-Birmanês e foi um dos editores finais dos Textos, Comentários e Sub-Comentários em Pali no Sexto Concílio Budista em 1954.<o:p></o:p></span></p>

<p class=Normal>Em 1979 ele viajou para os Estados Unidos da América com o Ven. Mahasi Sayadaw para ensinar o Dhamma e meditação tendo permanecido no país para continuar ensinando. Ele foi o abade do Dhammananda Vihara em Daly City na California, EUA. Ele também foi o diretor espiritual do Theravada Buddhist Society of America e de outros quatro centros Budistas no país.<o:p></o:p></span></p>

<p class=Normal>Ele foi o autor de sete livros em Birmanês e em inglês ele é o autor de The Four Foundations of Mindfulness publicado pela editora Wisdom em 1990.<o:p></o:p></span></p>

<p class=Normal>O Ven. Sayadaw U Silananda faleceu no dia 13 de Agosto de 2005.</p>

