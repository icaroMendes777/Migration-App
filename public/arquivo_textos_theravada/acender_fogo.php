completamente.<o:p></o:p></span></p>

<p class=Normal>S: Nesse caso, o poder do
entendimento é maior ou o da ignorância é que é maior?<o:p></o:p></span></p>

<p class=Normal>D: O poder da ignorância é
grande, senhor.<o:p></o:p></span></p>

<p class=Normal>S: Como pode ser grande? A
pobre saiu correndo tão rápido quanto podia; vocês nem conseguem vê-la mais.<o:p></o:p></span></p>

<p class=Normal>D: Mas ela volta sempre,
senhor.<o:p></o:p></span></p>

<p class=Normal>S: Isso acontece porque
vocês permitem que ela volte. Se vocês admitirem apenas o entendimento e a
sabedoria, a ignorância não conseguirá regressar. Mas se vocês consentirem que
ela volte, então pouco a pouco o entendimento se dissolverá e a ignorância
assumirá o controle uma vez mais. É como com as lâmpadas neste lugar em que
estamos. O que é necessário para ligar as luzes?<o:p></o:p></span></p>

<p class=Normal>D: Interruptores, senhor.<o:p></o:p></span></p>

<p class=Normal>S: O que ocorre dentro dos
interruptores para que tenhamos luz?<o:p></o:p></span></p>

<p class=Normal>D: A eletricidade flui
através deles, senhor.<o:p></o:p></span></p>

<p class=Normal>S: O que acontece se o
fluxo for interrompido?<o:p></o:p></span></p>

<p class=Normal>D: Ficará escuro, senhor.<o:p></o:p></span></p>

<p class=Normal>S: O que vocês precisam
fazer para converter a escuridão em claridade?<o:p></o:p></span></p>

<p class=Normal>D: Temos que alimentar os
bulbos com eletricidade, senhor.<o:p></o:p></span></p>

<p class=Normal>S: E para onde vai a
escuridão quando as luzes são acesas?<o:p></o:p></span></p>

<p class=Normal>D: Ela desaparece, senhor.<o:p></o:p></span></p>

<p class=Normal>S: Fica restando algo da
escuridão?<o:p></o:p></span></p>

<p class=Normal>D: Não, senhor.<o:p></o:p></span></p>

<p class=Normal>S: Quando o entendimento
brilha, fica restando algo da ignorância?<o:p></o:p></span></p>

<p class=Normal>D: Não, senhor.<o:p></o:p></span></p>

<p class=Normal>S: Nesse caso, o poder da
ignorância é grande?<o:p></o:p></span></p>

<p class=Normal>D: Não, senhor, não é.<o:p></o:p></span></p>

<p class=Normal>S: É difícil fazer o que
fizemos agora?<o:p></o:p></span></p>

<p class=Normal>D: Não é muito difícil,
senhor.<o:p></o:p></span></p>

<p class=Normal>S: Não vemos a realidade
quando de súbito surge a luz?<o:p></o:p></span></p>

<p class=Normal>D: Sim vemos, senhor.<o:p></o:p></span></p>

<p class=Normal>S: Vocês ainda serão capazes
de errar?<o:p></o:p></span></p>

<p class=Normal>D: Não, senhor.<o:p></o:p></span></p>

<p class=Normal>S: É tão fácil! O que os
discípulos do Buda compreenderam? Se vocês quiserem acender a luz, vocês terão
que conectar os fios para que a eletricidade flua. Então, de maneira suave
mantenha a sua atenção no objeto da meditação; ela se conectará. Vocês
entenderam?<o:p></o:p></span></p>

<p class=Normal>D: Gostaríamos de desistir
disto, senhor.<o:p></o:p></span></p>

<p class=Normal>S: Concentrem a atenção no
objeto. De forma suave. Vocês ficam cansados ao focar a mente dessa forma?<o:p></o:p></span></p>

<p class=Normal>D: Não senhor.<o:p></o:p></span></p>

<p class=Normal>S: Isso lhes custa algo?<o:p></o:p></span></p>

<p class=Normal>D: Não, senhor.<o:p></o:p></span></p>

<p class=Normal>S: Vocês precisam parar o
seu trabalho?<o:p></o:p></span></p>

<p class=Normal>D: Não, senhor.<o:p></o:p></span></p>

<p class=Normal>S: Isso não é maravilhoso?
Vocês podem praticar nas quatro posturas: sentados, em pé, andando e deitados.
Vocês podem manter o foco no ponto de toque da respiração com a atenção na
inspiração e expiração mesmo ao comer, beber e trabalhar?<o:p></o:p></span></p>

<p class=Normal>D: Por favor, senhor,
ensine como manter a atenção na respiração enquanto nos movemos e trabalhamos.<o:p></o:p></span></p>

<p class=Normal>S: Vocês conhecem muitas
técnicas distintas, mas vocês não precisam praticá-las todas. Escolham uma e
trabalhem com ela. Se vocês mantiverem a mente focada com firmeza num objeto,
de imediato vocês terão consciência daquilo que não tinham antes, como acontece
quando vocês vêm luz ao ligar o interruptor. Pode ainda haver entendimento
incorreto e delusão na suas mentes, enquanto estiverem assim conscientes?<o:p></o:p></span></p>

<p class=Normal>D: Não, senhor, mas como
não sabemos onde se encontram os interruptores de luz, temos que continuar
sentados no escuro.<o:p></o:p></span></p>

<p class=Normal>S: Mas como, se vocês têm
tantos interruptores! Qualquer um que vocês ligarem irá acender a luz.<o:p></o:p></span></p>

<p class=Normal>D: Senhor, como há tantos
interruptores, eu não sei qual ligar.<o:p></o:p></span></p>

<p class=Normal>S: Qualquer um serve; os
resultados serão imediatos.<o:p></o:p></span></p>

<p class=Normal>D: Quando ligo aquele
interruptor, senhor, a lâmpada queima de imediato. Por isso pensei que fosse o
interruptor errado.<o:p></o:p></span></p>

<p class=Normal>S: Não irá queimar; irá
acender a lâmpada. Mesmo que não acenda um dia, quando você tentar novamente,
com certeza<span style="mso-spacerun: yes">  </span>acenderá.<o:p></o:p></span></p>

<p class=Normal>D: Mas eu não sei como
ligar o interruptor, senhor.<o:p></o:p></span></p>

<p class=Normal>S: Vocês conhecem os
interruptores; um grande número de técnicas lhes foram ensinadas por monges
cheios de compaixão. Agora, não tentem todas elas. Selecionem apenas uma.<o:p></o:p></span></p>

<p class=Normal>D: Senhor, por favor ensine
essa única técnica!<o:p></o:p></span></p>

<p class=Normal>S: Todas as técnicas
ensinadas pelo Buda irão funcionar como interruptor para acender a luz.<o:p></o:p></span></p>

<p class=Normal>D: Gostaríamos de aprender
uma técnica através da qual pudéssemos aperfeiçoar a nossa conduta e pudéssemos
ser treinados no entendimento enquanto trabalhamos, andamos ou estivermos
sentados, senhor.<o:p></o:p></span></p>

<p class=Normal>S: Bem então, digam-me,
todos vocês, pequenos e grandes, respiram?<o:p></o:p></span></p>

<p class=Normal>D: Sim, senhor.<o:p></o:p></span></p>

<p class=Normal>S: Portanto, não existe aqui
ninguém que não saiba como respirar. Alguma vez vocês foram capazes de dizer,
“Desculpe-me, estou muito ocupado agora. Não tenho tempo para respirar”?<o:p></o:p></span></p>

<p class=Normal>D: Não senhor.<o:p></o:p></span></p>

<p class=Normal>S: Então, respirar é muito
fácil para vocês, não é? Agora, tentem descobrir por onde sai o ar quando vocês
expiram.<o:p></o:p></span></p>

<p class=Normal>D: Ele sai pelo nariz,
senhor.<o:p></o:p></span></p>

<p class=Normal>S: Existe algum outro lugar
por onde ele saia?<o:p></o:p></span></p>

<p class=Normal>D: Não, senhor, existe
apenas um lugar.<o:p></o:p></span></p>

<p class=Normal>S: Sim, existe apenas um
lugar. Então não me digam que existem tantos interruptores que vocês não sabem
qual deles ligar. Vocês estão todos respirando, não estão? Onde toca o ar
quando vocês expiram?<o:p></o:p></span></p>

<p class=Normal>D: Ele toca um ponto na
base do nariz, senhor.<o:p></o:p></span></p>

<p class=Normal>S: O que acontece quando o
ar entra? Onde ele toca?<o:p></o:p></span></p>

<p class=Normal>D: Ele toca no mesmo ponto,
senhor.<o:p></o:p></span></p>

<p class=Normal>S: Portanto, isso é bem
evidente para vocês: o ar toca num pequeno ponto na base do nariz quando vocês
inspiram e expiram. Vocês têm consciência disso, não é mesmo?<o:p></o:p></span></p>

<p class=Normal>D: Sim, senhor.<o:p></o:p></span></p>

<p class=Normal>S: Não permitam que a mente
perambule em busca de outros objetos. Vocês podem perceber o ponto onde o ar
toca como se o estivessem tocando com o dedo? Coloquem a sua atenção ali e
mantenham-na ali. Não sigam a respiração pelas narinas. Mantenham a atenção com
calma e tranqüilidade no mesmo ponto e vocês serão capazes de perceber o ar
entrando e saindo. O fluxo de ar é continuo, não é mesmo?<o:p></o:p></span></p>

<p class=Normal>D: Sim, senhor.<o:p></o:p></span></p>

<p class=Normal>S: Vocês podem ter
consciência dele sem a menor interrupção. Se mantiverem a atenção naquele
ponto, existirá apenas a consciência das propriedades da mentalidade-materialidade (nome e forma) <i style='mso-bidi-font-style:normal'>(nama-rupa). </i>Agora, em
qual desses dois se encaixam as narinas, mente ou matéria?<o:p></o:p></span></p>

<p class=Normal>D: Senhor, até onde sei, as
narinas são matéria <i style='mso-bidi-font-style:normal'>(rupa).<o:p></o:p></i></span></p>

<p class=Normal>S: Qual é a entidade que
conhece a sensação de toque?<o:p></o:p></span></p>

<p class=Normal>D: Senhor, aquilo que
conhece é a mente <i style='mso-bidi-font-style:normal'>(nama).<o:p></o:p></i></span></p>

<p class=Normal>S: Portanto, vocês estão
conscientes da mentalidade-materialidade (nome e forma) ao mesmo tempo. Se vocês têm
consciência da mentalidade-materialidade (nome e forma), existem ainda outras entidades das
quais vocês não tenham consciência? <o:p></o:p></span></p>

<p class=Normal>D: Não, senhor, não existe
nada além de mentalidade-materialidade (nome e forma).<o:p></o:p></span></p>

<p class=Normal>S: Estar consciente da mentalidade-materialidade (nome e forma) é ignorância ou entendimento?<o:p></o:p></span></p>

<p class=Normal>D: É entendimento, senhor.<o:p></o:p></span></p>

<p class=Normal>S: Se uma pessoa não tem
consciência da mentalidade-materialidade (nome e forma), como denominamos isso?<o:p></o:p></span></p>

<p class=Normal>D: Denominamos isso
ignorância, senhor.<o:p></o:p></span></p>

<p class=Normal>S: A ignorância poderá
influenciar-nos até mesmo enquanto estivermos treinando no entendimento?<o:p></o:p></span></p>

<p class=Normal>D: Não, senhor, não poderá.<o:p></o:p></span></p>

<p class=Normal>S: Ainda existirá motivo
para preocupação e temor sobre o presente e o futuro?<o:p></o:p></span></p>

<p class=Normal>D: Não, senhor, não haverá.<o:p></o:p></span></p>

<p class=Normal>S: Mesmo se vocês aplicarem
a atenção apenas por um instante, vocês se beneficiarão. Quanto benefício vocês
receberão se puderem manter essa atenção por mais tempo?<o:p></o:p></span></p>

<p class=Normal>D: Devem ser muitos os
benefícios, senhor.<o:p></o:p></span></p>

<p class=Normal>S: Haverá ainda alguma
dúvida nas suas mentes quanto à capacidade de vocês para atingirem o despertar
pelo qual aspiram?<o:p></o:p></span></p>

<p class=Normal>D: Não, senhor.<o:p></o:p></span></p>

<p class=Normal>S: Vocês poderão alcançar o
objetivo ainda mais rápido do que pensavam. É claro que vocês ainda têm que
cumprir com as suas responsabilidades em relação aos seus mestres, pais e
filhos. Vocês têm que apoiar os ensinamentos do Buda. Vocês têm que obter o seu
sustento. Se vocês não cumprirem com essas responsabilidades, a virtude de
vocês será perfeita?<o:p></o:p></span></p>

<p class=Normal>D: Não será, senhor.<o:p></o:p></span></p>

<p class=Normal>S: Se a virtude não for
perfeita vocês poderão alcançar o objetivo?<o:p></o:p></span></p>

<p class=Normal>D: Não, senhor, é
impossível.<o:p></o:p></span></p>

<p class=Normal>S: Diga-me, quando ou aonde
não seria possível praticar a conduta correta e a meditação ao mesmo tempo? <o:p></o:p></span></p>

<p class=Normal>D: Nunca é demasiado
difícil, senhor, até mesmo quando se está enfermo.<o:p></o:p></span></p>

<p class=Normal>S: Se os insights se
desenvolvem através da sua prática, vocês ainda precisam contar para os outros
que vocês se tornaram felizes através do Dhamma do Buda?<o:p></o:p></span></p>

<p class=Normal>D: Não é necessário contar,
senhor.<o:p></o:p></span></p>

<p class=Normal>S: E se vocês não falarem a
respeito, isso significa que vocês desconhecem a própria felicidade?<o:p></o:p></span></p>

<p class=Normal>D: Mesmo se não contarmos
para ninguém, ainda assim saberemos por nós mesmos, senhor.<o:p></o:p></span></p>

<p class=Normal>S: É da mesma maneira que
as pessoas nobres sabem. Vocês sabem por si mesmos o quanto conquistaram até
agora e quando alcançarem o objetivo, também saberão. Se vocês escreverem num
pedaço de papel que o sal é salgado e alguém ler isso, ela saberá que o sal é
salgado, não é mesmo?<o:p></o:p></span></p>

<p class=Normal>D: É claro, senhor.<o:p></o:p></span></p>

<p class=Normal>S: E se vocês contarem para
uma pessoa que o sal é salgado, ela saberá?<o:p></o:p></span></p>

<p class=Normal>D: Com certeza, senhor.<o:p></o:p></span></p>

<p class=Normal>S: Mas diga-me, o sal se
tornará salgado só pelo fato de você escrever ou dizer isso para alguém?<o:p></o:p></span></p>

<p class=Normal>D: Não, senhor, é claro que
não.<o:p></o:p></span></p>

<p class=Normal>S: Se vocês lerem que o sal
é salgado, vocês na verdade sabem que é assim?<o:p></o:p></span></p>

<p class=Normal>D: Embora a pessoa
compreenda que é salgado, ela na verdade não sabe qual é o sabor do sal.
Somente quando colocarmos um pouco de sal na língua e o experimentarmos,
poderemos saber o que “salgado” significa.<o:p></o:p></span></p>

<p class=Normal>S: Se vocês tiverem
experimentado e souberem que é salgado, vocês ainda terão que ler a respeito?
Vocês ainda terão que fazer declarações a respeito do sabor?<o:p></o:p></span></p>

<p class=Normal>D: Não, senhor.<o:p></o:p></span></p>

<p class=Normal>S: Se contarmos ao nosso
vizinho sobre o sabor, ele compreenderá?<o:p></o:p></span></p>

<p class=Normal>D: Ele apenas terá ouvido a
respeito, senhor.<o:p></o:p></span></p>

<p class=Normal>S: O que precisamos fazer
para que ele compreenda?<o:p></o:p></span></p>

<p class=Normal>D: Temos que lhe dar um
pouco de sal e fazer com que ele experimente. De outro modo, o que ele souber
será só de ouvir dizer, senhor.<o:p></o:p></span></p>

<p class=Normal>S: Vocês sabem, com
certeza, que a conduta correta e o insight e sabedoria constituem o caminho
para a libertação do sofrimento?<o:p></o:p></span></p>

<p class=Normal>D: Sim, senhor, nós
sabemos.<o:p></o:p></span></p>

<p class=Normal>S: Se vocês lerem que o sal
é salgado e considerarem que esse conhecimento é suficiente, então é nesse
ponto que vocês pararão. Mas se quiserem ter certeza, vocês têm que
experimentar por si mesmos. É suficiente ler que o sal é salgado e tê-lo
confirmado por mim?<o:p></o:p></span></p>

<p class=Normal>D: Senhor, você não mentiria.
Se você me dissesse que é salgado, isso seria o bastante para mim.<o:p></o:p></span></p>

<p class=Normal>S:<span
style="mso-spacerun: yes">  </span>Agora vocês estão voltando atrás naquilo que
disseram antes. Vocês sabem que o sal é salgado por ouvir e ler a respeito, mas
só se vocês souberem genuinamente por si mesmos é que ficarão satisfeitos. Se
vocês contarem para alguém que o sal é salgado e ele aceitar cegamente o que
vocês disseram, nesse caso ele não irá nem mesmo sentir desejo de experimentar
por ele mesmo.<o:p></o:p></span></p>

<p class=Normal>Afinal, ele pensa que sabe.
Com essa noção na mente, ele não verá necessidade de prová-lo. Não é fácil
saber por si mesmo que o sal é salgado. O sal existe. Tome-o, prove-o. Só
então, vocês saberão isso por si mesmos e não haverá necessidade de perguntar
aos outros.<o:p></o:p></span></p>

<p class=Normal>D: Senhor, ontem eu provei
um pouco de sal.<o:p></o:p></span></p>

<p class=Normal>S: Verdade? Porque só um
pouco? O sal acabou?<o:p></o:p></span></p>

<p class=Normal>D: Não, senhor, há muito
sal.<o:p></o:p></span></p>

<p class=Normal>S: Então tome-o! Não provem
apenas um pouco. Usem tanto quanto precisarem. Cada um de vocês tem sal, não
tem?<o:p></o:p></span></p>

<p class=Normal>D: Sim, senhor. Nós não
comemos até ficarmos saciados, senhor, e ficamos satisfeitos em provar apenas
um pouco.<o:p></o:p></span></p>

<p class=Normal>S: Mas é claro que vocês
não estão pensando em deixar por isso mesmo, estão?<o:p></o:p></span></p>

<p class=Normal>D: Bem, senhor, na verdade
não, mas à medida que o tempo passa tudo muda. Planejamos algo no ano passado e
um ano já passou...<o:p></o:p></span></p>

<p class=Normal>S: Mas desta vez não pensem
apenas. Quantos “pensadores” havia na época do Buda?<o:p></o:p></span></p>

<p class=Normal>D: Havia tantos quantos
grãos de areia na praia, senhor.<o:p></o:p></span></p>

<p class=Normal>S: Vocês precisam se esforçar mais e meditar. Vocês precisam se esforçar mais para compreender os
ensinamentos do Buda. Vocês estão planejando fazer isso, não estão? Vocês
também estão pensando em colocar esforço só nesta vida?<o:p></o:p></span></p>

<p class=Normal>D: Se só pensarmos nisso, senhor, novamente seremos
deixados para trás no ciclo de nascimento e morte.<o:p></o:p></span></p>

<p class=Normal>S: Bem, nesse caso então não há problema. “No passado,
nós nos perdemos porque só pensávamos em fazer esforço, mas agora compreendemos
que existe fogo nos dois gravetos de madeira. Iremos esfregá-los um no outro.”
Pensando dessa forma, haverá esforço e também o desejo de enfrentar essa
batalha...Vocês já se apossaram dos dois gravetos de madeira? Se eu continuar
falando, vocês irão pensar, “Esse monge já está falando faz muito tempo.” Eu
devo parar agora. Só por meio do empenho poderão conseguir algo, se tiverem o
desejo de trabalhar, meditar, trabalhar duro, aplicar-se com o mesmo esforço e
determinação dos Nobres de antigamente.<o:p></o:p></span></p>

