<p class=Normal>S: Isso demora muito?<o:p></o:p></span></p>

<p class=Normal>D: Não muito tempo, senhor.<o:p></o:p></span></p>

<p class=Normal>S: Não, se essa pessoa
trabalhar com determinação, não irá demorar muito tempo. É exatamente o mesmo
com essa nossa prática. Você quer fogo. Você sabe que se esfregar dois gravetos
de madeira irá obter fogo. Agora, se você contar, “Uma esfregada, duas
esfregadas...” irá ficar um pouco quente. E então você descansa um pouco. Você
irá acender o fogo?<o:p></o:p></span></p>

<p class=Normal>D: Não, senhor.<o:p></o:p></span></p>

<p class=Normal>S: OK, então você começa
outra vez, uma duas, três vezes...e mais uma vez o calor é produzido. Então
você se deita novamente para descansar. Você irá acender o fogo?<o:p></o:p></span></p>

<p class=Normal>D: Não, senhor.<o:p></o:p></span></p>

<p class=Normal>S: E se você continuar
