<p class=Normal>D: Não iremos obter fogo.<o:p></o:p></span></p>

<p class=Normal>S: E se você continuar
agindo assim durante todo um ano?<o:p></o:p></span></p>

<p class=Normal>D: Apenas irá esquentar,
