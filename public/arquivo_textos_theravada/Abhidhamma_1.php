<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>(a)
Consciência resultante insalubre <i>(akusalavipakacittani)<o:p></o:p></i></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Compreende sete
tipos de consciência que resultam de <i>kamma </i>insalubre. Esses tipos de
consciência não são insalubres em si, trata-se de consciências resultantes
produzidas por <i>kamma </i>insalubre; a palavra insalubre qualifica não esses
estados de consciência em si, mas o <i>kamma </i>do qual elas nasceram.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Consciência
no olho <i>(cakkhuviññana)</i>:</span></b><span lang=PT-BR style='mso-ansi-language:
PT-BR'> Os primeiros cinco tipos de consciência resultante, em ambas as
classes, insalubres e saudáveis, são aqueles baseados na matéria sensível, <i>(pasada),
</i>do olho, ouvido, nariz, língua e corpo. Esse tipo de consciência surge com
base no fenômeno sensitivo de cada órgão do sentido, (veja o <a
href="Abhidhamma_3.php#sensitivo">III. Compêndio da Matéria – Fenômenos
Sensitivos</a> para mais detalhes). A sua função é a simples cognição dos seus
respectivos objetos. No caso de resultantes insalubres, os objetos são
desagradáveis ou indesejados. No entanto, o impacto do objeto nos primeiros
quatro sentidos é fraco, então a sensação associada é neutra, isto é,
equanimidade. Mas no caso da consciência resultante insalubre no corpo, o
impacto do objeto na faculdade do corpo é forte e dessa forma a sensação
associada é dolorosa.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR>Consciência Receptora <i>(sampaticchanacitta): </i></b></span><span
lang=PT-BR style='font-weight:normal'>Quando um objeto sensual causa um impacto
ou colide com uma faculdade do sentido numa das cinco portas dos sentidos,
exemplo, uma forma visível no olho, primeiro surge uma <i>citta </i>que adverte
para o objeto. Imediatamente depois disso, surge a consciência no olho que vê o
objeto. Esse ato de ver dura apenas um momento mental. Imediatamente em
seguida, surge uma <i>citta</i> que compreende ou “recebe” o objeto que foi
visto pela consciência no olho. Essa é a consciência receptora que resulta do
mesmo tipo de <i>kamma </i>que produziu a consciência no olho.<o:p></o:p></span></h1>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Consciência
Investigadora <i>(santiranacitta)</i>: </span></b><span lang=PT-BR
style='mso-ansi-language:PT-BR'>Esta consciência surge imediatamente após a
consciência receptora. A sua função é investigar ou examinar o objeto. Esta
consciência, bem como a consciência receptora, surge apenas nos cinco meios dos
sentidos e ambas são resultado de <i>kamma</i> passado.<o:p></o:p></span></p>


<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>(b) Consciência
resultante saudável <i>(kusalavipaka-ahetukacittani)<o:p></o:p></i></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Compreende
oito tipos de consciência que resultam de <i>kamma </i>saudável.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>(c)
Consciência funcional <i>(ahetukakiriya-cittani)<o:p></o:p></i></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Este tipo
de consciência não é resultado de <i>kamma</i>. Elas realizam tarefas que não
possuem potencial para gerar <i>kamma. </i>São, portanto, tipos de consciência
que não são nem resultado de kamma, nem produzem kamma. <o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR>Advertência nas cinco portas <i>(pancadvaravajjanacitta)</i></b>:
</span><span lang=PT-BR style='font-weight:normal'>Quando um objeto sensual
causa impacto ou colide com uma faculdade do sentido numa das cinco portas dos
sentidos, antes que a consciência apropriada possa surgir, por exemplo a
consciência no olho que vê uma forma visível, uma outra consciência precisa ter
surgido primeiro. Essa consciência é a advertência nas cinco portas, que possui
a função de advertir, avisar, fazer observar o objeto que esteja se
apresentando<span style="mso-spacerun: yes">  </span>numa das cinco portas dos
sentidos.<o:p></o:p></span></h1>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Advertência
na mente <i>(manodvaravajjanacitta)</i>: </span></b><span lang=PT-BR
style='mso-ansi-language:PT-BR'>Este tipo de consciência pode surgir ou num
processo cognitivo que esteja ocorrendo nas cinco portas dos sentidos, ou num
processo que esteja ocorrendo na porta da mente. Em cada caso a sua função é
distinta. Quando ocorre nas cinco portas é denominada consciência determinante.
A sua função é determinar ou definir o objeto que foi percebido pela
consciência nos sentidos. A consciência determinante vem em seguida à
consciência investigadora. Depois que a consciência investigadora examinou o
objeto, a consciência determinante o discrimina. Quando<span
style="mso-spacerun: yes">  </span>na porta da mente, este tipo de consciência
desempenha a função de advertência para o objeto que está surgindo na porta da
mente. <o:p></o:p></span></p>


<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Nota</span></b><span
lang=PT-BR style='mso-ansi-language:PT-BR'>: Depois de estudar o <a
href="Abhidhamma_4.php">IV. Compêndio dos Processos Cognitivos</a>, as
descrições das consciências resultantes e funcionais ficarão mais claras. <o:p></o:p></span></p>

<p class=Normal><b> 1.3 <i>Cittas</i> Belas (<i>sobhanacittani</i>)<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Esses tipos
de consciências são denominadas de belas porque estão acompanhadas de fatores
mentais, <i>(cetasikas), </i>belos. (Veja o <a href="Abhidhamma_2.php#belos">II.
Compêndio dos Fatores Mentais – Fatores Belos</a>). Deve ser entendido que o
belo, <i>(sobhana), </i>possui uma extensão mais ampla do que o saudável, <i>(kusala).
</i>O belo inclui todas as <i>cittas</i> saudáveis, e também inclui as <i>cittas
</i>resultantes e funcionais que possuem fatores mentais belos. Além das <i>cittas
</i>da esfera sensual, (24 no total), as <i>cittas </i>belas compreendem também
todas as <i>cittas </i>da esfera da matéria sutil, da esfera imaterial e
supramundanas.<o:p></o:p></span></p>


