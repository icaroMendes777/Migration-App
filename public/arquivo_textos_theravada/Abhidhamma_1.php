<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>(a)
Consciência com raiz na Cobiça (<i>lobhamulacittani</i>) <o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A palavra
em Pali, <i>lobha,</i> inclui todas as variações de cobiça, desde a paixão mais
intensa e avareza até o apreço e o apego sutil. A consciência com<span
style="mso-spacerun: yes">  </span>raiz na cobiça está dividida em oito tipos
com base em três princípios de dicotomia. O primeiro é a sensação concomitante,
(<i>vedana</i>), quer seja uma sensação de alegria ou equanimidade; o segundo é
a presença ou ausência do entendimento incorreto; o terceiro é baseado na
consideração de se a <i>citta</i> é estimulada ou não estimulada. Da permutação
dessas três distinções surgem oito tipos de consciência mostrados na tabela
acima.<span style='mso-bidi-font-weight:bold'><o:p></o:p></span></span></p>

<p class=Normal><b style='mso-bidi-font-weight:normal'><span lang=PT-BR
style='mso-ansi-language:PT-BR'>Acompanhada pela alegria,</span></b><span
lang=PT-BR style='mso-ansi-language:PT-BR'> (<i style='mso-bidi-font-style:
normal'>somanassasahagata</i>): A palavra <i style='mso-bidi-font-style:normal'>somanassa</i>,
alegria, é derivada de <i style='mso-bidi-font-style:normal'>su</i>=prazeroso
e<span style="mso-spacerun: yes">  </span><i style='mso-bidi-font-style:normal'>manas</i>=mente;
significando assim, literalmente, um estado mental prazeroso. Toda consciência
é acompanhada por algum tipo de sensação que pode ser corporal ou mental; prazerosa,
dolorosa ou neutra. <i>Somanassa</i> é uma sensação mental, ao invés de
corporal, e é prazerosa, ao invés de dolorosa ou neutra. Equanimidade, (<i
style='mso-bidi-font-style:normal'>upekha</i>), neste caso quer dizer uma
sensação neutra, uma sensação mental que não se inclina nem para a alegria, nem
para a dor.<o:p></o:p></span></p>

<p class=Normal><b style='mso-bidi-font-weight:normal'><span lang=PT-BR
style='mso-ansi-language:PT-BR'>Associada ao entendimento incorreto, </span></b><span
lang=PT-BR style='mso-ansi-language:PT-BR'>(<i style='mso-bidi-font-style:normal'>ditthigatasampayutta</i>):
A palavra <i>ditthi</i> quer dizer entendimento e se não estiver acompanhada do
prefixo <i>samma,</i> (correto), em geral se refere ao entendimento incorreto.
O entendimento incorreto acompanha a consciência com raiz na cobiça sob a forma
de idéias, crenças, convicções, opiniões, racionalizações. O entendimento
incorreto, do qual a consciência surge,<span style="mso-spacerun: yes"> 
</span>pode reforçar o apego provendo a consciência de uma justificativa
racional, ou o entendimento em si pode ser um objeto de apego. Quando a
consciência está dissociada do entendimento incorreto significa que ela está
desacompanhada de qualquer justificativa proporcionada pelo entendimento
incorreto.<o:p></o:p></span></p>

<p class=Normal><b style='mso-bidi-font-weight:normal'><span lang=PT-BR
style='mso-ansi-language:PT-BR'>Estimulada,</span></b><span lang=PT-BR
style='mso-ansi-language:PT-BR'> (<i style='mso-bidi-font-style:normal'>sankharika</i>):
ou instigada, induzida, a partir do exterior ou do interior. O estímulo pode
ser corporal, verbal ou puramente mental. <o:p></o:p></span></p>


<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>(b)
Consciência com raiz na Raiva (<i style='mso-bidi-font-style:normal'>dosamulacittani</i>)
<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Existem
dois tipos de consciência nesta classe, distinguidas como estimuladas ou não estimuladas.
As consciências com<span style="mso-spacerun: yes">  </span>raiz na raiva
surgem apenas com a sensação de desprazer, (<i style='mso-bidi-font-style:normal'>domanassa</i>),
uma sensação mental desagradável. Essa sensação acompanha apenas a consciência
com raiz na raiva. Ao contrário da consciência com raiz na cobiça, a consciência
com raiz na raiva não surge associada ao entendimento incorreto. Embora o
entendimento incorreto possa motivar atos com base na raiva, de acordo com o
Abhidhamma, o entendimento incorreto não surge simultaneamente com a raiva, na
mesma <i>citta</i>, mas antes, num tipo diferente de <i>citta</i>. A
consciência com raiz na raiva está associada à aversão, (<i style='mso-bidi-font-style:
normal'>patigha</i>). <i style='mso-bidi-font-style:normal'>Patigha</i> inclui
todas as graduações de aversão, do ódio violento até a sutil irritação. <i
style='mso-bidi-font-style:normal'>Patigha</i> significa em termos literais
“golpear contra,” indicando um atitude mental de resistência, rejeição ou
destruição.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Embora o
desprazer e a aversão estejam sempre juntos, as suas qualidades devem ser
distinguidas. O desprazer é a experiência de uma sensação desagradável, a
aversão é a atitude mental de má vontade ou irritação. Em relação aos cinco
agregados, o desprazer é incluído no agregado da sensação, (<i
style='mso-bidi-font-style:normal'>vedanakkhandha</i>), enquanto que a aversão
é incluída no agregado das formações mentais, (<i style='mso-bidi-font-style:
normal'>sankharakkhandha</i>).<o:p></o:p></span></p>


<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>(c)
Consciência com raiz na delusão (<i style='mso-bidi-font-style:normal'>mohamulacitta</i>)
<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Esta última
classe de consciência insalubre compreende aquelas <i style='mso-bidi-font-style:
normal'>cittas</i> nas quais as outras duas raízes – cobiça e raiva estão
ausentes. Somente a delusão está presente. Este tipo de consciência está sempre
acompanhada pela equanimidade, por isso uma sensação mental agradável ou
desagradável não surge em relação a um objeto. Existem dois tipos de
consciência nesta classe, uma associada à dúvida, (perplexidade, ceticismo,
indecisão), e a outra associada à inquietação, (distração mental, agitação). <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>De acordo com
o Abhidhamma, a inquietação é encontrada em todos os doze tipos de consciência
insalubre mas nas primeiras onze <i style='mso-bidi-font-style:normal'>cittas</i>
a sua força é relativamente mais fraca e a sua função é secundária. Já neste
último tipo de <i style='mso-bidi-font-style:normal'>citta</i> a inquietação se
torna um fator preponderante.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Exemplos: <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os oito
tipos de consciências com raiz na cobiça podem ser exemplificados da seguinte
forma, (de acordo com a numeração da tabela acima):<o:p></o:p></span></p>

<p class=Normal>1 &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; Com
alegria, um menino, com a idéia de que não existe mal em roubar, rouba
espontaneamente uma maçã de uma feira.<br>

2 &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; Com
alegria, um menino, tendo a mesma idéia, rouba uma maçã estimulado por um
amigo.<br>

3-4 &nbsp; &nbsp; &nbsp; &nbsp;Igual
a 1 e 2, exceto pelo fato de que o menino não tem nenhuma idéia e age por
impulso.<br>

5-8 &nbsp; &nbsp; &nbsp; &nbsp;Estas
quatro são semelhantes a 1-4, exceto pelo fato de que o roubo é cometido com
uma sensação neutra.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os dois
tipos de consciências com raiz na raiva podem ser exemplificados da seguinte
forma:<o:p></o:p></span></p>

<p class=Normal>9 &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; Com raiva um homem assassina um outro
num ataque espontâneo de ódio.<br>

10 &nbsp; &nbsp; &nbsp; &nbsp;Com raiva um homem assassina um outro
depois de ter premeditado.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os dois
tipos de consciências com raiz na delusão podem ser exemplificados da seguinte
forma:<o:p></o:p></span></p>

<p class=Normal>11 &nbsp; &nbsp; &nbsp; &nbsp;Uma
pessoa, devido à delusão, duvida da iluminação do Buda ou da eficácia do
Dhamma.<br>

12 &nbsp; &nbsp; &nbsp; &nbsp;Uma
pessoa é tão distraída que não é capaz de focar a mente em qualquer objeto.<o:p></o:p></span></p>

<p class=Normal><b> 1.2 <i>Cittas</i> sem Raiz (<i>ahetukacittani</i>)<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A palavra <i>ahetuka
</i>significa sem raiz e caracteriza aqueles tipos de consciência que estão
desprovidos dos fatores mentais chamados <i>hetu, </i>raízes. Esses tipos,
dezoito no total, não contêm nenhuma das três raízes insalubres – cobiça, raiva
