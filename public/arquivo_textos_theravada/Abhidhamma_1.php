<p class=Tit1 align=center style='text-align:center'>I. Compêndio da Consciência <i>(Citta)</i></Tit1>



<p class=Normal>&nbsp;</p>

<hr size=2 width="100%" align=center>

<p class=Normal>&nbsp;</p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O primeiro
capítulo do <i>Abhidhammattha Sangaha</i> é dedicado à análise de <i>citta</i>,
consciência, a primeira das quatro realidades últimas. A palavra em Pali <i>citta</i>
é derivada da raiz <i>citi</i>, que significa cognição, conhecimento. Os
comentários definem <i>citta</i> de três formas: como agente, como instrumento
e como atividade. Como agente, <i>citta</i> é aquilo que conhece ou percebe um
objeto. Como instrumento, <i>citta</i> é aquilo através do qual os fatores
mentais, <i>(cetasikas), </i>que a acompanham conhecem ou percebem um objeto.
Como atividade, <i>citta</i> é em si mesma nada mais que o processo de cognição
de um objeto. A terceira definição, em relação à pura atividade, é considerada
como a mais adequada das três: isto é, <i>citta</i> é fundamentalmente um
processo de cognição ou de conhecimento de um objeto. Não existe um agente ou
instrumento que possui em si mesmo uma identidade separada do processo de
cognição. As definições como agente e como instrumento são para refutar a idéia
incorreta daqueles que crêem que um eu ou ego permanente seja o agente ou
instrumento da cognição. Aquilo que realiza o ato da cognição não é um eu mas
sim a consciência ou <i>citta</i>, e esse ato de cognição é necessariamente
impermanente, marcado pelo surgimento e cessação.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Para
elucidar a natureza de qualquer realidade última ou fenômeno, os comentários em
Pali sugerem quatro dispositivos que são: (1) a sua característica, (<i>lakkhana</i>),
isto é, a qualidade que mais se destaca no fenômeno; (2) a sua função, (<i>rasa</i>),
a realização de uma tarefa concreta ou de um objetivo; (3) a sua manifestação,
(<i>paccupatthana</i>), a forma como o fenômeno se apresenta na experiência;
(4) a sua causa mais próxima, (<i>padatthana</i>), a principal condição da qual
o fenômeno depende.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>No caso de <i>citta</i>,
a sua característica é conhecer um objeto, (<i>vijanana</i>). A sua função é ser
o precursor dos fatores mentais, <i>(cetasikas), </i>no sentido de que os
preside e está sempre acompanhada por eles. A sua manifestação – a forma como
se mostra para o meditador – é como um contínuo de processos, (<i>sandhana</i>).
A sua causa mais próxima é a mentalidade-materialidade (nome e forma), (<i>nama-rupa</i>),
porque a consciência não pode surgir só, com a completa ausência de fatores
mentais, <i>(cetasikas)</i>,<i> </i>e fenômenos materiais.<o:p></o:p></span></p>
    
<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Aquilo que
normalmente tomamos por consciência é na verdade uma série de <i>cittas</i>,
atos momentâneos de consciência, que ocorrem numa sucessão extremamente rápida,
que não podem ser detectados pela mente comum.<o:p></o:p></span></p>

<p class=Normal><b><a name=T11></a> Classificações da Consciência (<i>Citta</i>)<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Existem
vários princípios para a classificação de <i>citta</i>. <o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>O
primeiro deles toma por base o plano, (<i>bhumi</i>), da consciência</span></b><span
lang=PT-BR style='mso-ansi-language:PT-BR'>. Existem quatro planos da
consciência. Três são mundanos: a esfera sensual, a esfera da materialidade
sutil e a esfera imaterial; o quarto plano é supramundano. A palavra esfera e plano
estão conectadas, porém não têm o mesmo significado. As esferas da consciência
são categorias para classificar tipos de <i>cittas</i>, os planos de existência
são reinos ou mundos nos quais os seres renascem e nos quais vivem. No entanto
uma relação clara existe entre as esferas da consciência e os planos de
existência: uma esfera da consciência em particular compreende aqueles tipos de
consciência que são típicos do plano de existência correspondente e que <i
style='mso-bidi-font-style:normal'>freqüentam </i>aquele plano, tendendo a
surgir ali com mais freqüência. Contudo, a consciência de uma esfera em
particular não está atada ao plano correspondente, e poderá surgir em outros
planos de existência também; por exemplo, <i>cittas</i> da esfera da
materialidade sutil ou da esfera imaterial podem surgir no plano sensual e <i>cittas</i>
da esfera sensual podem surgir nos planos da materialidade sutil ou nos planos
imateriais. Mas, apesar disso, a conexão existe no sentido de que a consciência
duma esfera é típica do plano que possui o mesmo nome.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Consciência
da esfera sensual, (<i>kamavacaracitta</i>): A palavra <i>kama</i> significa
ambos, a sensualidade subjetiva, isto é, o desejo por prazeres sensuais, e a
sensualidade objetiva, isto é, os cinco objetos sensuais externos – formas
visíveis, sons, aromas, sabores e tangíveis. O <i>kamabhumi</i> é o plano da
existência sensual que compreende onze reinos – os quatro estados de privação,
o reino humano e os seis paraísos sensuais.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Consciência
da esfera da materialidade sutil, (<i>rupavacaracitta</i>): a esfera da
materialidade sutil é a consciência que corresponde ao plano de existência da
materialidade sutil, (<i>rupabhumi</i>), ou a consciência que está ligada aos
estados de absorção meditativa conhecido como <i>rupajjhanas</i>. Os <i>rupajjhanas</i>
são assim chamados porque em geral são alcançados através da meditação da
concentração num objeto material, (<i>rupa</i>), que pode ser uma <i>kasina</i>
ou partes do próprio corpo, (por ex: a respiração). Esse objeto se torna a base
sobre a qual os <i>jhanas</i> são desenvolvidos. Os estados de consciência
exaltados que são alcançados com base nesses objetos são chamados <i>rupavacaracitta</i>,
consciência da esfera da materialidade sutil.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Consciência
da esfera imaterial, (<i>arupavacaracitta</i>): a esfera imaterial é a
consciência que corresponde ao plano de existência imaterial, (<i>arupabhumi</i>),
ou a consciência que está ligada às absorções imateriais – os <i>arupajjhanas</i>.
Ao meditar para alcançar os estados meditativos desprovidos de forma, que se
encontram além dos <i>rupajjhanas</i>, é necessário descartar todos os objetos
conectados com a forma material e focar apenas em algum objeto não material,
tal como o espaço infinito, etc. Os estados de consciência exaltados que são
alcançados com base nesses objetos são chamados <i>arupavacaracitta</i>,
consciência da esfera imaterial.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Consciência
supramundana, (<i>lokuttaracitta</i>): a palavra <i>lokuttara</i>,
supramundana, é derivada de<i> loka</i>=mundo, e <i>uttara</i>=além,
transcendente. Podem existir<span style="mso-spacerun: yes">  </span>três
conceitos de “mundo”: o mundo dos seres vivos, o universo físico e o mundo das
formações, isto é, a totalidade dos fenômenos condicionados, materiais e mentais.
A noção de mundo relevante neste caso é o mundo das formações, isto é, todos os
fenômenos mundanos incluídos como parte dos cinco agregados do apego. Aquilo
que transcende o mundo condicionado é o elemento incondicionado, Nibbana. Os tipos
de consciência que diretamente alcançam a realização de Nibbana são chamados <i>lokuttaracitta</i>,
consciência supramundana. Os outros três tipos são chamados <i>lokiyacitta</i>,
consciência mundana.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>O
segundo princípio de classificação, que desempenha um importante papel na
filosofia do Abhidhamma, é descrito de acordo com o tipo ou natureza, (<i>jati</i>),
da consciência.<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Com relação
à sua natureza, a consciência pode ser dividida em quatro classes: insalubre,
saudável, resultante e funcional. A consciência insalubre, (prejudicial,
inábil, <i>akusalacitta</i>), é a consciência que é acompanhada pelas três
raízes insalubres – cobiça, raiva e delusão. Esse tipo de consciência é chamada
de insalubre porque ela prejudica a saúde mental, é passível de crítica sob o
ponto de vista ético e produz resultados dolorosos. A consciência saudável,
(benéfica, hábil, <i>kusalacitta</i>), é a consciência que é acompanhada pelas
três raízes saudáveis – não-cobiça ou generosidade, não-raiva ou amor bondade,
e não-delusão ou sabedoria. Esse tipo de consciência favorece a saúde mental,
não é passível de crítica sob o ponto de vista ético e produz resultados
prazerosos.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Tanto as
consciências saudáveis como as insalubres produzem <i>kamma</i>, ação volitiva
ou intencional. Aquelas <i>cittas</i> ou estados de consciência que surgem
através do amadurecimento ou fruto de <i>kamma</i> são chamadas de resultantes,
(<i>vipaka</i>). Estas constituem uma terceira classe de <i>citta</i> distinta
das duas primeiras, uma classe que compreende tanto os resultados de <i>kamma</i><span
style='mso-bidi-font-style:italic'> </span>saudável como insalubre. Deve ser
compreendido que ambos os <i>kamma</i><span style='mso-bidi-font-style:italic'>,
saudável e insalubre,</span> e os seus resultados, são puramente mentais. <i>Kamma</i>
é a ação intencional volitiva associada às <i>cittas</i> saudáveis ou
insalubres; os seus resultados ou frutos são outras <i>cittas</i> que vivenciam
a maturação de kamma.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A quarta
classe de consciência é chamada em Pali de <i>kirya</i> ou <i>kriya</i>,
interpretado como ‘funcional.’ Este tipo de consciência não produz <i>kamma,</i>
nem é resultado de <i>kamma</i>. Ela envolve atividade, mas essa atividade não
é capaz de produzir <i>kamma</i>.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Tanto as
consciências resultantes, como as funcionais, não são saudáveis, nem
insalubres. Ao invés disso, elas são classificadas como indeterminadas, isto é,
estados de consciência que não podem ser determinados com base na dicotomia
saudável/insalubre.<o:p></o:p></span></p>

