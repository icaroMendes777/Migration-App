<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>As <i>cittas</i>
saudáveis da esfera sensual estão divididas em oito tipos com base em três princípios
de dicotomia. O primeiro princípio é a sensação concomitante, que em quatro
casos é alegria, <i>(somanassa)</i>, isto é, uma sensação mental prazerosa; e
nos outros quatro casos é a equanimidade, <i>(upekha), </i>isto é, uma sensação
mental neutra;<span style="mso-spacerun: yes">  </span>o segundo é a presença
ou ausência de conhecimento; e o terceiro é baseado na consideração de se a <i>citta</i>
é estimulada ou não estimulada.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Associada
ao conhecimento, <i>(ñanasampayutta):</i></span></b><span lang=PT-BR
style='mso-ansi-language:PT-BR'> o conhecimento significa ver as coisas como
elas na verdade são. Na consciência associada ao conhecimento, a palavra <i>ñana</i>
se refere ao fator mental da sabedoria, <i>(pañña-cetasika)</i>, que também
representa a raiz da não-delusão, <i>(amoha). </i>A consciência <i>dissociada
do conhecimento </i>não contém o fator da sabedoria, mas ela não envolve a ignorância,
<i>(avijja),</i> ou a delusão, <i>(moha)</i>, que estão presentes somente nas <i>cittas</i>
insalubres.<o:p></o:p></span></p>

<p class=Normal><b style='mso-bidi-font-weight:normal'><span lang=PT-BR
style='mso-ansi-language:PT-BR'>Estimulada,</span></b><span lang=PT-BR
style='mso-ansi-language:PT-BR'> <b>(<i style='mso-bidi-font-style:normal'>sankharika</i>)</b>:
ou instigada, induzida, a partir do exterior ou do interior. O estímulo pode
ser corporal, verbal ou puramente mental.<o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Com
raízes, <i>(sahetuka): </i></span></b><span lang=PT-BR style='mso-ansi-language:
PT-BR'>As quatro <i>cittas </i>saudáveis associadas ao conhecimento possuem
todas as três raízes saudáveis; as quatro dissociadas do conhecimento possuem
como raízes a não-cobiça ou generosidade e a não-raiva ou amor bondade, mas
lhes falta a não-delusão.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Exemplos: <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os oito
tipos de consciências saudáveis da esfera sensual podem ser exemplificados da
seguinte forma, (de acordo com a numeração da tabela acima):<o:p></o:p></span></p>

<p class=Normal>1 &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;
 Alguém
com alegria pratica um ato generoso, compreendendo que isso é uma ação
saudável, de modo espontâneo sem ser estimulado.<o:p></o:p></span></p>

<p class=Normal>2 &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;
Alguém
realiza a mesma boa ação, com compreensão, depois de deliberação ou estímulo de
alguém.<o:p></o:p></span></p>

<p class=Normal>3 &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;
Alguém
com alegria pratica um ato generoso, sem ser estimulado, mas sem a compreensão
de que essa é uma ação saudável.<o:p></o:p></span></p>

<p class=Normal> 4 &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;
Alguém
com alegria pratica um ato generoso, sem compreensão, depois de deliberação ou
estímulo de alguém.<o:p></o:p></span></p>

<p class=Normal> 5-8 &nbsp; &nbsp; &nbsp; &nbsp; Esses tipos de consciência seguem os
exemplos acima, mas com uma sensação neutra no lugar da alegria.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Esses oito
tipos de consciência são denominados saudáveis porque elas inibem as
contaminações e produzem bons resultados. Elas surgem em seres mundanos, <i>(puthujjana),</i>
e seres em treinamento, <i>(<a name=PVW>sekha</a>), </i>– nobres discípulos nos
três primeiros estágios do caminho espiritual – sempre que eles praticarem ações
corporais e verbais saudáveis e sempre que gerarem estados mentais saudáveis
relacionados com a esfera sensual. Essas <i>cittas</i> não surgem em <i>arahants,
</i>cujas ações estão desprovidas de potencial de <i>kamma.<o:p></o:p></i></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os
demais tipos de <i>cittas</i> da esfera mundana, (materialidade sutil e
imaterial), bem como as <i>cittas</i> supramundanas, não serão discutidas neste
trabalho.<o:p></o:p></span></b></p>

