<p class=Normal>O <a href="../sutta/DN15.php">Mahanidana Sutta</a> é um dos discursos mais profundos do Cânone em Pali. O discurso trata de modo amplo os ensinamentos da origem dependente (<i>paticca samuppada</i>) e não-eu (<i>anatta</i>) traçando o contexto do funcionamento desses ensinamentos na prática. </p>

<p class=Normal>A primeira parte do discurso toma os fatores da origem dependente na seqüência do efeito para a causa, seguindo passo a passo até a dependência mútua por um lado da mentalidade-materialidade, (atividade mental e física), e por outro lado da consciência. Com relação a esse ponto, vale a pena observar que a palavra “grande” no título do discurso pode ter um duplo significado: modificando a palavra “discurso” – sendo um discurso longo – e modificando “causas,” referindo-se ao fato que materialidade-mentalidade e a consciência como fatores causais são capazes de explicar tudo aquilo que pode ser descrito no mundo. </p>

<p class=Normal>Depois de identificar a seqüência básica dos fatores no processo causal, o discurso então analisa a sua inter-relação, mostrando como eles podem explicar o sofrimento ambos no nível individual e social. </p>

<p class=Normal>A segunda parte do discurso, abordando o ensinamento sobre não-eu, mostra como a origem dependente proporciona na prática um foco para esse ensinamento. Começando com a seção sobre as Descrições do Eu, classificando os vários modos em que a noção de um “eu” pode ser definida em termos da forma. O esquema de análise introduzida nesta seção – classificando as idéias de um eu de acordo com as variáveis: com forma ou sem forma; finito ou infinito; já existente ou desenvolvendo-se no futuro; e, alterável através do esforço humano – abrange todas as teorias do eu propostas nos clássicos Upanisads, bem como todas as teorias de um eu, ou alma, propostas em tempos mais recentes. A inclusão nesta lista de um eu infinito nega a crença que os ensinamentos do Buda sobre não-eu negam nada mais do que a noção de um eu “separado” ou “limitado.” Este discurso indica que até mesmo uma noção de um eu ilimitado, infinito, abrangente está baseada numa obsessão mental que deve ser abandonada. </p>

<p class=Normal>A seção seguinte sobre as Não Descrições do Eu, mostra que é possível que a mente funcione sem colocar um “eu” na experiência. As seções restantes focam nos modos em que isso pode ser feito tratando a noção do eu na medida em que esta se relaciona aos diferentes aspectos da mentalidade-materialidade. A primeira dessas seções – Considerações do Eu – foca na noção do eu relacionada com as sensações, um dos fatores da mentalidade no composto mentalidade-materialidade. A seguinte seção - As Sete Estações da Consciência – foca na forma, no sem forma e na percepção, que é um outro elemento da mentalidade, que cria o espaço para que a consciência pouse e se expanda no nível “macro” no ciclo de morte e renascimento. A última seção - As Oito libertações – focam na forma, no sem forma e na percepção no nível “micro,” na prática das absorções meditativas (<i>jhana</i>). </p>

<p class=Normal>Em cada um desses casos, uma vez que a noção de apego e identificação com a mentalidade-materialidade possa ser rompida, a dependência mútua entre a consciência e a mentalidade-materialidade também é rompida. Isto resulta na completa libertação dos limites de “dentro desse alcance que há um caminho para a designação, para a linguagem, para a conceituação ... dentro desse alcance que há uma esfera para a sabedoria ... dentro desse alcance que o ciclo gira para descrever este estado de ser/existir, isto é, quando há a mentalidade-materialidade junto com a consciência.” Essa é a libertação para a qual apontam os ensinamentos do Buda. </p>


