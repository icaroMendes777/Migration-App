<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A formulação dos
ensinamentos do Buda mais comum e a mais conhecida é aquela que ele mesmo
anunciou no Primeiro Discurso em Benares, a fórmula das Quatro Nobres Verdades.
O Buda declarou que essas verdades transmitem em poucas palavras toda a
informação básica que necessitamos para iniciar o caminho da libertação. Ele
diz que tal como a pegada do elefante, por causa do seu grande tamanho, contém
as pegadas de todos os demais animais, da mesma forma as Quatro Nobres
Verdades, por causa da sua abrangência, contêm dentro de si todos os
ensinamentos benéficos e saudáveis. No entanto, enquanto que muitos expositores
do Budismo devotaram atenção à explicação do conteúdo das quatro verdades,
somente raramente se dá alguma consideração à razão porque elas são denominadas
<i>nobres </i>verdades. No entanto é justamente esse termo descritivo
&quot;nobre&quot; que nos revela porque o Buda escolheu colocar o seu
ensinamento nesse formato específico, e é o mesmo termo que nos permite
experimentar, mesmo à distância, o sabor único que permeia toda a doutrina e
disciplina do Iluminado. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A palavra &quot;nobre&quot;
ou <i>ariya,</i> é usada pelo Buda para designar um tipo particular de pessoa,
o tipo de pessoa que os seus ensinamentos têm como objetivo criar. Nos
discursos o Buda classifica os seres humanos em duas categorias amplas. De um
lado estão os <i>puthujjanas,</i> os mundanos, aqueles que pertencem à massa,
cujos olhos ainda estão cobertos pela poeira das contaminações e delusão. Do
outro lado estão os <i>ariyans,</i> os nobres, a elite espiritual, que obtém
esse status não pelo nascimento, situação social ou autoridade eclesiástica mas
da sua nobreza interior de caráter. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Esses dois tipos gerais não
estão separados um do outro por um abismo intransponível, cada um confinado a
um compartimento hermeticamente fechado. Uma série de gradações pode ser
distinguida ascendendo do nível mais sombrio do mundano cego preso no calabouço
do egoísmo e afirmação pessoal, passando pelo estágio do mundano virtuoso em
quem as sementes da sabedoria estão começando a brotar, e mais além através dos
estágios intermediários dos nobres discípulos até o indivíduo aperfeiçoado no
cume de toda a escala do desenvolvimento humano. Esse é o Arahant, o libertado,
que absorveu a visão purificadora da verdade tão profundamente que todas suas
contaminações foram eliminadas, e com elas, o sofrimento. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Apesar de que o caminho do
cativeiro à libertação, do mundano à nobreza espiritual, é um caminho graduado
envolvendo prática gradual e progresso gradual, não é um processo contínuo
uniforme. O progresso ocorre a passos discretos, e num certo ponto – o ponto
que separa o status de um mundano daquele de um nobre – uma abertura é
encontrada que precisa ser atravessada, não simplesmente com mais um passo
adiante, mas fazendo um salto deste lado para a outra margem. Esse evento
decisivo no desenvolvimento interior do praticante, esse salto radical que
impulsiona o discípulo do domínio e linhagem do mundano para o domínio e
linhagem dos nobres, ocorre precisamente através da penetração das Quatro
Nobres Verdades. Isso nos revela a importante razão pela qual as quatro
verdades reveladas pelo Buda são denominadas nobres verdades. Elas são nobres
verdades porque quando nós as penetramos até o seu cerne, quando compreendemos
o seu real significado e implicações, deixamos de lado o status de mundano e
adquirimos o status de um nobre, extraídos da massa anônima para a comunidade
dos discípulos do Abençoado unidos por uma visão única e inabalável. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Antes da penetração das
verdades, por mais que estejamos dotados de virtudes espirituais, ainda não
estamos em território seguro. Não estamos imunes à regressão, ainda sem
garantia de libertação, não somos invencíveis no nosso esforço no caminho. As
virtudes de um mundano são virtudes tênues. Elas podem se desenvolver ou
desaparecer, elas podem florescer ou declinar, e correspondendo ao seu grau de
força nós podemos subir ou cair no nosso movimento através do ciclo de existências.
Quando as nossas virtudes são plenas nós poderemos subir e permanecer na bem
aventurança entre os devas; quando as nossas virtudes declinam ou nosso mérito
se exauriu podemos novamente afundar nas profundidades miseráveis. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Porém, com a penetração das
verdades nós saltamos através do golfo que nos separa do grupo dos nobres. O
olho do <span style="mso-spacerun: yes"> </span>Dhamma é aberto, a visão da
verdade é revelada, e apesar de que a vitória decisiva ainda não foi obtida, o
caminho para o objetivo final está aos nossos pés e a segurança suprema do
cativeiro paira no horizonte. Aquele que compreendeu as verdades mudou de
linhagem, cruzou do domínio do mundano para o domínio dos nobres. Tal discípulo
é incapaz de regredir às fileiras dos mundanos, incapaz de perder a visão da
verdade que brilhou ante o seu olho interior. O progresso em direção ao
objetivo final, a erradicação completa da ignorância e do desejo, pode ser
lenta ou rápida; pode ocorrer facilmente ou como resultado de intensa batalha.
Porém, não importa quanto tempo leve, ou o grau de facilidade com que se
avance, uma coisa é certa: tal discípulo que viu as Quatro Nobres Verdades com
clareza imaculada não poderá jamais regredir, não perderá jamais o status de
nobre, e está destinado a alcançar o fruto final do estado de arahant em no
máximo sete vidas.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A razão porque a penetração
das Quatro Nobres Verdades pode conferir essa imutável nobreza de espírito está
implícita nas quatro tarefas que nos são impostas. Tomando essas tarefas como o
desafio da nossa vida – o desafio como discípulos do Iluminado – sem importar o
estágio de desenvolvimento a partir do qual iniciamos, podemos gradualmente
avançar em direção à infalível compreensão dos nobres. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A primeira nobre verdade, a
verdade do sofrimento, deve ser plenamente entendida: a tarefa que nos é dada é
o completo entendimento. Uma marca característica dos nobres é que eles não
seguem pelo curso da vida de modo impensado, mas se esforçam em compreender a
existência à partir de dentro de si, da maneira mais honesta e completa
possível. Para nós, também, é necessário refletir acerca da natureza da nossa
vida. Nós precisamos examinar a fundo o profundo significado de uma existência
atada por um lado pelo nascimento e do outro pela morte, e sujeita a todos os
tipos de sofrimento detalhados pelo Buda nos seus discursos. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A segunda nobre verdade, a verdade
da origem do sofrimento, significa a tarefa do abandono. Um nobre é um nobre
porque ele iniciou o processo de eliminação das contaminações que estão na raiz
do sofrimento, e nós também, se aspiramos alcançar o nível dos nobres, devemos
estar preparados para resistir ao encanto sedutor das contaminações. Enquanto
que a erradicação do desejo somente pode ser obtida com os caminhos supramundanos,
mesmo no desenrolar diário de nossas vidas mundanas podemos aprender a moderar
as manifestações mais grosseiras das contaminações, e através da apurada auto
observação podemos gradualmente afrouxar o seu controle sobre os nossos
corações. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A terceira nobre verdade, a
verdade da cessação do sofrimento, significa a tarefa da realização. Apesar de
que Nibbana, a extinção do sofrimento, pode ser somente realizado pessoalmente
pelos nobres, a confiança que depositamos no Dhamma como nosso guia para a vida
nos mostra o que deveríamos selecionar como aspiração final, como o que em última
instância possui mais valor. Uma vez que tenhamos entendido o fato de que todas
as coisas condicionadas no mundo, sendo impermanentes e sem substância, não
podem jamais nos proporcionar plena satisfação, podemos então elevar nosso
objetivo para o elemento incondicionado, Nibbana o Imortal, e fazer dessa
aspiração o marco ao redor do qual organizamos nossas escolhas e preocupações
do dia a dia. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Finalmente, a quarta nobre
verdade, o Nobre Caminho Óctuplo, nos proporciona a tarefa do desenvolvimento.
Os nobres alcançaram o seu status pelo desenvolvimento do caminho óctuplo, e
enquanto que somente os nobres estão seguros de nunca desviar do caminho, os
ensinamentos do Buda nos proporcionam as instruções meticulosas que
necessitamos para trilhar o caminho culminando no plano dos nobres. Esse é o
caminho que faz nascer a visão, que faz nascer o conhecimento, que conduz à
compreensão mais elevada, iluminação e Nibbana, a realização máxima dos nobres.
<o:p></o:p></span></p>


