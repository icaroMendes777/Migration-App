<p class=Normal>No <a href="../sutta/MN101.php">Devadaha Sutta</a> o Buda refuta as teorias dos Jainas – aqui chamados de Niganthas – uma ordem de contemplativos que florescia na Índia naquela época. Embora superficialmente este sutta aparenta ter um interesse puramente histórico, ele destaca dois pontos importantes que são bastante relevantes no que toca alguns mal entendidos comuns sobre o Budismo na atualidade. </p>

 

<p class=Normal>O primeiro ponto diz respeito ao ensinamento sobre a ação, ou <i>kamma</i> (<i>karma</i>). O entendimento geral desse ensinamento é que as ações do passado determinam o prazer ou a dor no presente, enquanto que as ações no presente determinam o prazer e a dor no futuro. Ou, para mencionar um livro recente sobre esse tópico, “<i>Karma</i> é o princípio moral que governa a conduta humana. Este declara que a nossa experiência no presente é condicionada pela nossa conduta no passado e que a nossa conduta no presente irá condicionar a nossa experiência no futuro.” Isso, no entanto, não descreve com precisão o ensinamento do Buda sobre <i>karma</i> e ao contrário é um relato bastante acurado do ensinamento dos Niganthas, que o Buda refuta de modo explícito neste sutta. Ao questionar os Niganthas, o Buda argumenta que se todo o prazer e a dor experimentados no presente forem determinados pelas ações passadas, porque é que eles agora sentem dores quando se dedicam às duras práticas ascéticas, mas não as sentem quando não se dedicam a essas práticas? Se a ação no passado fosse o único fator determinante, então a ação no presente não deveria ter nenhum efeito na experiência de prazer ou dor no presente. </p>

 

<p class=Normal>Desse modo, o Buda destaca uma das características mais particulares do seu próprio ensinamento sobre <i>karma</i>: que a experiência no presente de prazer ou dor é o resultado combinado de ambas ações, no passado e no presente. Esta aparente pequena adição à noção de <i>karma</i> desempenha um enorme papel ao permitir o exercício do livre arbítrio e a possibilidade de dar um fim ao sofrimento antes que os efeitos de todas ações passadas tenham frutificado. Em outras palavras, essa adição é o que viabiliza a prática Budista e faz com seja possível que uma pessoa que tenha completado a prática sobreviva e a ensine aos outros com plena autoridade. </p>

 

<p class=Normal>O segundo ponto importante abordado neste sutta — como dar um fim à dor e ao sofrimento — está relacionado com o primeiro. Se a causa do sofrimento no presente estivesse localizada exclusivamente no passado, ninguém poderia fazer nada no momento presente para parar esse sofrimento; o máximo que poderia ser feito seria suportar o sofrimento enquanto que nenhum novo <i>karma</i> que conduza a novo sofrimento fosse criado. Embora essa fosse a abordagem dos Jainas, muitas pessoas no presente acreditam que essa também é a abordagem Budista. A meditação, de acordo com esse entendimento, é o processo de purificar a mente do <i>karma</i> antigo treinando-a para observar a dor que surge com equanimidade não reativa. A dor é o resultado do <i>karma</i> antigo, a equanimidade não adiciona nenhum <i>karma</i> novo e assim, ao longo do tempo, todo o <i>karma</i> antigo poderá ser extinto. </p>

 

<p class=Normal>Neste sutta, no entanto, o Buda ridiculariza essa idéia. Primeiro ele observa que nenhum dos Niganthas nunca chegou ao fim do sofrimento tentando extingui-lo dessa forma; depois ele observa que a crença nessa prática estava totalmente baseada na fé no mestre deles e a aceitação das suas idéias, mas nem a fé e tampouco a aceitação de idéias podem servir como garantias da verdade. Ele então emprega o símile do homem atingido por uma flecha para ilustrar que apenas alguém que tenha sucedido em superar o sofrimento pode estar numa posição para falar com autoridade sobre o método que de fato dá um fim ao sofrimento. (O que não é mencionado neste sutta é a idéia dos Niganthas que a prática das austeridades teria êxito em extinguir por completo o <i>karma</i> antigo, se culminasse no suicídio por inanição. Desse modo não poderia haver ninguém vivo que pudesse testemunhar quanto à eficácia desse método). </p>

 

<p class=Normal>O Buda então relata como a meditação na verdade funciona para dar um fim à dor e ao sofrimento. A sua análise mostra que o problema por detrás da dor não é a ação passada, mas a paixão – no presente – que é a causa da dor. Em outras palavras, a dor não é inevitável. O sofrimento no presente pode ser evitado mudando o próprio entendimento e a própria atitude com relação à causa do sofrimento no presente. O Buda ilustra esse princípio com o símile de um homem apaixonado por uma mulher: enquanto ele sentir paixão por ela, ele irá sofrer ao vê-la desfrutar da companhia de um outro homem; quando, ao ver a conexão entre o seu sofrimento e a sua paixão, ele abandona aquela paixão, ele não mais sofre por causa daquilo. </p>

 

<p class=Normal>Por conseguinte a prática precisa focar nos meios para compreender e produzir o desapego das causas de dor e sofrimento no aqui e agora. Tal como apontado pelo Buda no <a href="../sutta/MN106.php">MN 106</a>, a equanimidade desempenha um importante papel nesta prática, mas esta também pode se tornar um objeto de paixão e deleite, que então obstruirá a verdadeira libertação. Assim ele observa aqui que, em alguns casos, o desapego pode surgir simplesmente ao observar diretamente com equanimidade as causas do sofrimento. Em outros casos, o desapego apenas irá ocorrer através do esforço: o esforço mental – através das fabricações do pensamento aplicado, do pensamento sustentado e da percepção – para desenvolver o discernimento necessário para penetrar e abandonar toda e qualquer paixão. </p>

 

<p class=Normal>O restante do sutta está dedicado a um esquema padrão sobre como a prática se desenvolve ao longo do tempo, mostrando como a combinação apropriada de observação equânime mais o esforço e as fabricações, podem conduzir ao desapego e através do desapego à libertação de todo o sofrimento. </p>

 

 

