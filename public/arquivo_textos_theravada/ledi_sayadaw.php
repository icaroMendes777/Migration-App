<p class=Tit1 align=center style='text-align:center'>Ledi Sayadaw</Tit1>

<p class=Normal> Bhikkhu Nyana, que mais tarde se tornaria conhecido como Ledi Sayadaw (1846-1923), foi um dos mais destacados monges Budistas de Myanmar (antiga Birmânia) da era moderna, reconhecido no mundo Budista Theravada como um acadêmico, escritor e mestre de meditação. Ele se ordenou como samanera ainda jovem e aos 20 anos foi ordenado como bhikkhu. Mais tarde ele percorreu muitas regiões da antiga Birmânia com o propósito de propagar o Dhamma do Buda. Nas cidades e vilarejos que visitava além de proferir discursos do Dhamma ele também ensinava o Abhidhamma e meditação. Alguns dos centros de meditação estabelecidos por ele existem até os dias de hoje.<o:p></o:p></span></p>

<p class=Normal> Autor de mais de setenta manuais, dos quais oito foram traduzidos para o inglês, ele se tornou o ‘pai’ da moderna tradição de meditação de insight que se espalhou de Myanmar para todo o mundo. Ele recebeu o título de <i>Aggamaha pandita</i> do governo da Índia em 1911 e mais tarde a Universidade de Rangoon lhe conferiu o título de Doutor em Literatura <i>(Honoris Causa)</i>. Mesmo tendo um grande conhecimento das escrituras Budistas, ele tinha um talento especial para escrever numa linguagem viva e direta, com um estilo cintilante, adornando as suas explicações da doutrina Budista com símiles originais e esclarecedores.<o:p></o:p></span></p>



