<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>Pergunta 1:</b>
Os kammas dos pais determinam ou influenciam os kammas dos seus filhos? (Nota:
No aspecto fisiológico, os filhos herdam as características físicas dos pais).<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>Resposta:</b>:
Em termos físicos, os kammas dos filhos em geral são determinados pelos kammas
dos pais. Assim, pais saudáveis em geral geram descendentes saudáveis e pais
sem saúde acabam gerando descendentes sem saúde. Por outro lado, sob o ponto de
vista moral, o kamma do pai ou da mãe não afeta de nenhuma forma ou determina o
kamma da criança. O kamma da criança é algo à parte – ele forma a
individualidade da criança, a soma total dos seus méritos e deméritos
acumulados em inumeráveis existências anteriores. Por exemplo, o kamma do
Bodisatva, Príncipe Siddattha, com certeza não foi influenciado pelo kamma dos
seus pais, o rei Suddhodana e a sua esposa, a rainha Maya. O kamma glorioso e
poderoso do Bodisatva transcendia os kammas dos seus pais que em conjunto eram
menos potentes que o dele.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>Pergunta 2:</b>
Se os kammas dos pais não influenciam o kamma dos filhos, como pode ser
explicado o fato de que pais que sofrem de certas enfermidades fatais são
capazes de transmitir esse mal para os seus descendentes?<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>Resposta:</b>:
No caso em que uma criança herda esse tipo de enfermidade isso se deve à força
das características dos pais, devido à força <i>utu </i>(condições favoráveis
para a germinação) dos pais. Tome, por exemplo, duas sementes de uma planta
jovem, uma delas em uma terra seca, de qualidade inferior; a outra em uma terra
úmida, rica. O resultado, iremos descobrir, será que a primeira semente irá
germinar uma planta enferma e em breve irá mostrar os sintomas da enfermidade e
de deterioração, enquanto que a outra semente terá sucesso e crescerá,
convertendo-se numa árvore alta e saudável. Pode ser observado que o par de
sementes, com a mesma origem, crescem de forma distinta de acordo com o solo no
qual elas foram colocadas. O kamma passado de uma criança (tomando o caso dos
seres humanos) pode ser comparado com a semente; a disposição física da mãe
sendo o solo; e a disposição física do pai sendo a umidade que fertiliza o
solo. A grosso modo, para ilustrar nosso tema, diremos que, tomando a
existência e o desenvolvimento da germinação da muda como uma unidade, a
semente representa cerca de um décimo dessa unidade, o solo seis décimos e a
umidade o restante, três décimos. Portanto, embora o poder de germinação exista
em potencial na semente (a criança), o seu crescimento é determinado e
acelerado de forma poderosa pelo solo (a mãe) e pela umidade (o pai).<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Portanto,
ao mesmo tempo que as condições do solo e a umidade devem ser tomadas como
fatores em grande parte responsáveis pelo crescimento e condição da planta, as
influências dos pais (ou progenitores no caso do mundo animal) também devem ser
consideradas<span style="mso-spacerun: yes">  </span>em relação à concepção e
desenvolvimento do seu descendente.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os pais (ou
progenitores) participam no Kamma que determina os fatores físicos dos seus
descendentes da seguinte forma: se eles são seres humanos, então o seu
descendente será um ser humano. Se são reses, então o descendente será da
espécie deles. Se os seres humanos forem chineses, então o seu descendente será
da mesma raça. Assim, os descendentes são invariavelmente do mesmo gênero e
espécie, etc., daquele dos seus progenitores. Disso que foi dito, pode ser
visto que, embora o kamma de uma criança possa ser muito poderoso em si mesmo,
ele não estará totalmente isento da influência do kamma dos pais. Ela estará
sujeita a herdar as características físicas dos pais. Mas pode ocorrer que,
pelo fato do kamma da criança ser excepcionalmente forte, a influência conjunta
do kamma dos pais não seja capaz de ofuscá-lo. É claro, quase não é necessário
mencionar, que as más influências (físicas) dos pais também podem ser
compensadas através da aplicação da ciência médica.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Todos os
seres que nascem por meio da relação sexual contêm o efeito resultante de três
forças – primeira, o kamma passado de existências anteriores; segunda, o fluido
seminal da mãe; terceira, o fluido seminal do pai. As disposições físicas dos pais
podem ou não ser equivalentes em força. Uma pode cancelar a outra em maior ou
menor medida. O kamma da criança e as características físicas tais como raça,
cor, etc., serão o produto dessas três forças.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>Pergunta 3:</b>
Com a morte de um ser senciente, existe uma ‘alma’ que perambula de acordo com
a sua vontade?<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>Resposta:</b>:
Quando um ser senciente deixa uma existência existe o renascimento como um ser
humano, um Deva, um Brahma, um animal ou como um ser em uma das regiões do
inferno. Os céticos e as pessoas ignorantes acreditam que existem estágios
intermediários – <i>Antarabhava</i> – entre esses planos; e que existem seres
que não pertencem nem ao mundo dos humanos, dos devas ou dos brahmas, nem a
algum dos estados de existência reconhecidos nas escrituras, mas que se
encontram em estados intermediários. Alguns afirmam que esses seres
transicionais possuem os cinco <i>khandhas. </i>Alguns afirmam que esses seres
são ‘almas’ desencarnadas ou espíritos sem um invólucro material e outros dizem
que eles possuem a faculdade da visão como os devas e mais, que eles possuem o
poder de mudar de acordo com a sua vontade, em intervalos curtos, de uma
existência para qualquer uma outra das mencionadas antes. Outros ainda
acreditam na teoria fantástica e equivocada de que esses seres podem conceber
estar numa outra existência, distinta desta na qual eles na verdade estão;
assim, tomando por exemplo um desses seres, ele é uma pessoa pobre mas no
entanto se imagina rico. Ele pode se encontrar no inferno mas imagina estar no
reino dos devas, e assim por diante. A crença em estágios intermediários entre
existências é falsa e é condenada nos ensinamentos Budistas. Um ser humano
nesta vida, que devido ao seu kamma está destinado a ser um ser humano na
próxima existência irá renascer como tal; quem por seu kamma estiver destinado
a ser um deva na próxima existência irá reaparecer no plano dos devas e aquele
cuja vida futura deverá ser no inferno, será encontrado em uma das regiões do
inferno na próxima existência.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A idéia de
uma entidade ou ‘alma’ ou espírito, ‘indo’, ‘vindo’, ‘mudando’, ‘transmigrando’
de uma existência para outra é mantida pelo ignorante e com certeza não
encontra justificativa no Dhamma: não existe esse tipo de coisas, indo, vindo,
mudando, etc. entre existências. O conceito que está de acordo com o Dhamma
pode talvez ser ilustrado com a imagem projetada pelo projetor de filme e o som
emitido pelo aparelho de som e a sua relação com o filme e o disco
respectivamente.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Por
exemplo, um ser humano morre e renasce no plano dos devas. Embora essas duas
existências sejam distintas, o vínculo de continuidade entre as duas não é
rompido com o evento da morte. Da mesma forma com o ser cuja existência futura
será no inferno mais baixo. A distância entre o inferno e o plano humano parece
ser grande, no entanto, a continuidade de uma existência para outra não é
interrompida e não existe matéria ou espaço que possa intervir interrompendo a
tendência do kamma desse ser, do plano dos humanos para a região dos infernos.
A passagem de uma existência para outra é instantânea e a transição é muito
mais rápida que o piscar dos olhos ou o lampejo de um raio.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O kamma
determina o plano de renascimento, e o estado da existência nesse plano, de todos
os seres transitórios (que se encontram no ciclo de existências que deve ser
percorrido até alcançar Nibbana).<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os
resultados de kamma são muitos e podem ter vários tipos de consequências. As
oferendas religiosas (<i>dana) </i>podem assegurar para um ser humano o
privilégio de renascer como um ser humano, ou como um deva, num dos seis mundos
dos devas de acordo com o mérito das ações realizadas. E assim também com a
observação das regras de comportamento ético <i>(sila).</i> Os oito <i>jhanas</i>
ou estágios de iluminação são encontrados nos mundos de brahma até o seu nível
máximo que é o vigésimo mundo de brahma. E da mesma forma com as ações ruins,
cujos perpetradores são encontrados em cada um dos níveis até a maior
profundeza do inferno mais baixo. Portanto, nossos kammas no passado, presente
e futuro foram, são e serão a soma total das nossas ações boas, indiferentes ou
ruins. Como será visto a seguir, os nossos kammas determinam as mudanças nas
nossas existências.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Espíritos
ruins” não são portanto seres num estágio intermediário ou transicional de
existência, mas são realmente seres inferiores e eles pertencem a um dos cinco
planos de existência a seguir, os seres humanos, os devas, os infernos, animais
e os petas. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Eles estão
muito próximos do mundo dos seres humanos. Como a condição deles é infeliz,
eles são considerados de forma popular como espíritos ruins. Não é verdade que
todos aqueles que morrem neste mundo renascem como espíritos ruins, embora os
seres humanos que morram repentinamente ou de forma violenta estão propensos a
renascer nos planos inferiores dos devas.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>Pergunta 4:</b>:
