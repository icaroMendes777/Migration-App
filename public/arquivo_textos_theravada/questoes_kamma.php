<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>Pergunta 4:</b>:
Quais são os cinco <i>Abhiññana? </i>Eles podem ser alcançados apenas pelo
Buda?<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>Resposta:</b>:
Os cinco <i>Abhiññana </i>(poderes psíquicos) são:<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><i>Iddhividha,</i> </span><span lang=PT-BR style='font-style:
normal'>Poder Criador<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><i><span lang=PT-BR style='mso-ansi-language:PT-BR'>Dibbasota,
</span></i><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ouvido Divino<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><i><span lang=PT-BR style='mso-ansi-language:PT-BR'>Cittapariya-nana,
</span></i><span lang=PT-BR style='mso-ansi-language:PT-BR'>Conhecimento dos
pensamentos dos outros<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><i><span lang=PT-BR style='mso-ansi-language:PT-BR'>Pubbenivasanussati,
</span></i><span lang=PT-BR style='mso-ansi-language:PT-BR'>Conhecimento das
existências passadas<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><i>Dibbacakkhu,</i> Olho
Divino<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os cinco <i>Abhiññana
</i>são também alcançados pelos Arahants e Ariyas e também por mortais comuns
que praticam de acordo com as escrituras; como foi o caso dos eremitas, etc.,
que existiram antes do Buda e que eram capazes de voar através do espaço e
atravessar mundos distintos.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Nas
escrituras encontramos, com clareza, os meios para alcançar os cinco <i>abhiññana;
</i>e mesmo nos dias de hoje, se esses meios forem desenvolvidos com cuidado e
perseverança será possível alcançá-los. O fato de não vermos pessoas dotadas
com os cinco <i>abhiññana</i> nos dias de hoje é devido à falta do esforço
tenaz físico e mental para a sua realização.<o:p></o:p></span></p>

