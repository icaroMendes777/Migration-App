<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'><b>Resposta:</b>:
Com certeza, essa não é uma ocorrência incomum e está de acordo com os
ensinamentos Budistas com respeito ao kamma. Esse tipo de pessoa é chamada <i>jatisara
puggalo, jati </i>existência, <i>sara </i>lembrança e<i> puggalo </i>ser
racional.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os tipos
relacionados a seguir, que formam a ampla maioria dos seres humanos, são
incapazes de lembrar das suas existências passadas, se e quando renascidos como
seres humanos.<o:p></o:p></span></p>

