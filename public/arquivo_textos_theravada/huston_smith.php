<p class=Tit1 align=center style='text-align:center'>Huston Smith</Tit1>

<p class=Normal>Filho de pais missionários metodistas, Huston Smith nasceu na China e aos 17 anos veio para os Estados Unidos para cursar a universidade. Quando criança pensou que se tornaria um pastor de igreja, mas logo percebeu que o púlpito não ajudaria a saciar a sua vasta curiosidade.  Foi na Universidade de Chicago que ele começou  o seu trabalho para a obtenção do seu Ph.D. na cadeira de teísmo naturalista – um sistema filosófico no qual a religião intervém somente quando a ciência não tem as respostas. Ao contrário da maioria dos acadêmicos na sua área, ele nunca se contentou em  estudar as religiões do mundo como uma atividade secundária, ele sempre  mergulhou de corpo e alma no assunto.<o:p></o:p></span></p>

<p class=Normal>Escrever e falar sobre o que ele chama de “tradições sábias” é o que fez de Huston Smith, 81 anos, ser conhecido mundialmente. Ao longo da sua extensa carreira, no entanto, ele nunca trabalhou na obscuridade. No início da década de 1950, suas aulas sobre as religiões do mundo na Universidade Washington de St. Louis  se tornaram tão populares que uma estação de televisão pediu-lhe que desenvolvesse palestras para um programa  que seria levado ao ar em caráter nacional. Como resultado dessa série de entrevistas surgiu o seu livro mais lido, <i>The Religions of Man,</i> publicado pela Harper pela primeira vez em 1958 e mais tarde intitulado<i> The World’s Religions </i>(Traduzido para o Português como As Religiões do Mundo: Nossas Grandes Tradições de Sabedoria, publicado pela Cultrix). Esse livro já vendeu perto de dois milhões de cópias e tem sido usado, nos últimos 30 anos, como texto básico nos cursos de religião em escolas secundárias e universidades americanas. Seu capítulo sobre Sakyamuni Buddha se tornou uma das forças que impulsionaram a disseminação do interesse pelo budismo no Ocidente. Um companheiro clássico para esse livro é <i> Forgotten Truth </i> publicado pela Harper em1992, que descreve a unidade que fundamenta muitas tradições religiosas no mundo.<o:p></o:p></span></p>

<p class=Normal>Desde então ele escreveu, foi co-autor, editor e compilador de 12 livros para grandes e pequenos editores, e dentre eles se inclui, <i>Cleansing the Doors of Perception: The Religious Significance of Entheogenic Plants and Chemicals</i> (Putnam/Tarcher, 2000) e <i>One Nation Under God: The Triumph of the Native American Church,</i> com Reuben Snake (Clear Light Publishers, 1996). Seu livro mais recente é, <i>Why Religion Matters: The Fate of Human Spirit in an Age of  Disbelief </i> (Harper SanFrancisco, 2000).<o:p></o:p></span></p>

<p class=Normal>A respeitada carreira de professor de Huston Smith inclui postos na Universidade de Washington, no MIT (Massachusetts Institute of Technology) e na Universidade de Syracuse. Atualmente ele ensina na Universidade da California em Berkeley, numa aposentadoria bastante atarefada.<o:p></o:p></span></p>

