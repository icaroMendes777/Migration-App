<p class=Tit1 align=center style='text-align:center'>Bhante Yogavacara Rahula</Tit1>

<p class=Normal>Bhante Yogavacara Rahula nasceu como Scott Joseph DuPrez no sul da Califórnia em 1948. Ele cresceu durante a revolução dos hippies e serviu no exército americano durante três anos sendo que em 1967 esteve dez meses no Vietnã. Adotando o estilo de vida de um hippie errante ele iniciou na Escandinávia uma longa odisséia que o conduziu até a Índia e o Nepal. No Nepal ele encontrou os seus primeiros mestres espirituais, lamas tibetanos em um retiro de meditação de um mês, ao final do qual ele estava de certa forma convertido ao Budismo e comprometido com a busca da Verdade. A sua busca o conduziu ao Sri Lanka onde ele se ordenou em 1975 como noviço no Gothama Thapovanaya em Kalupaluwawa. Ele foi ordenado como monge em 1979 no Wat Thai em Los Angeles, Califórnia. Ele permaneceu no Sri Lanka até 1986 retornando aos EUA e vivendo no Bhavana Society até 2010, um monastério das florestas e centro de meditação localizado em West Virginia.<o:p></o:p></span></p>

<p class=Normal>Ele conduz retiros de meditação vipassana integrados com exercícios de Yoga nos EUA, Alemanha e outras localidades. Alem da sua autobiografia, <i>One Night's Shelter,</i> ele também escreveu um livro sobre os ensinamentos do Buda intitulado<i> The Way to Peace and Happiness</i> e um livro sobre meditação intitulado <i>Meditation - Body & Mind Connection</i>.<o:p></o:p></span></p>

<p class=Normal>Bhante Rahula já esteve no Brasil conduzindo retiros de meditação e palestras em várias cidades.<o:p></o:p></span></p>

