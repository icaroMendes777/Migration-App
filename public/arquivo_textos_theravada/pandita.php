<p class=Tit1 align=center style='text-align:center'>Sayadaw U Pandita </Tit1>

<p class=Normal>O Venerável Sayadaw U Pandita nasceu no vilarejo de Tadagale, que atualmente faz parte da região metropolitana de Rangoon na antiga Birmânia, Myanmar na atualidade, em 28 de Julho de 1921. Ele iniciou a sua educação Budista aos 7 anos de idade sob a orientação do Ven. Sayadaw U Zagara no monastério Dekkhinayone, tendo sido aprovado nos exames orais das escrituras Pahtamagne e Pahtamalat, (Nível Primário e Secundário). Aos 12 anos de idade ele foi ordenado como samanera no vilarejo de Ohne onde estudou o Pitaka básico e o Abhidhammattha Sangaha. Aos 18 anos ele foi aprovado no exame de Pathamagyi, (Nível Superior), no monastério Mahabodhi na vila de Kyauktan. Aos 20 anos de idade ele foi ordenado como bhikkhu. </p> 

<p class=Normal>Em 1948, depois do seu terceiro vassa, ele estudou o Pariyatti Dhamma no monastério Kyaikkasan Shwekyin e foi aprovado no exame de Pathamagyi e no exame especial de Pariyatti organizados pela Associação Zediyankana. Entre 1951 e 1952 ele foi aprovado nos exames organizados pelo Governo obtendo então, o grau de Dhammacariya. </p> 

<p class=Normal>Em 1950 foi quando U Pandita começou a praticar a meditação Vipassana sob as instruções do Venerável Aggamahapandita <a href="mahasi_sayadaw.php">Mahasi Sayadaw</a>. Nesse ano também ele passou a servir como professor do novo monastério Pattiyatti Shwegyin Kyaikkasan. Durante o Sexto Concílio Budista, ocorrido em Rangoon de 1954 a 1956, ele desempenhou o papel de Revisor (Pali Visodhaka). </p>

<p class=Normal>Em 1955, ele deixou o trabalho de professor do Dhamma passando a se dedicar integralmente à prática da meditação. Nessa época também, ele assumiu algumas das responsabilidades atribuídas a Mahasi Sayadaw no Thathana Yeiktha Meditation Center em Rangoon. Em 1959 ele acompanhou Mahasi Saydaw ao Sri Lanka onde permaneceu por três anos abrindo ali novos centros de meditação. De volta a Myanmar, ele se tornou o principal professor de meditação em três centros da tradição de Mahasi Sayadaw – em  Rangoon, Moulmein e Mandalay – para monásticos e leigos.  

<p class=Normal>Com o falecimento de Mahasi Sayadaw em 1982, o Ven. Sayadaw U Pandita foi escolhido como o Professor Principal, (Ovadacariya), do Thathana Yeiktha Meditation Center em Rangoon, que é o principal centro da tradição de Mahasi Sayadaw. </p> 

<p class=Normal>O Ven. Sayadaw U Pandita é um renomado mestre de meditação e tornou bastante conhecido no Ocidente depois da sua primeira visita aos Estados Unidos em 1984, quando dirigiu um retiro de meditação no Retreat Center do Insight Meditation Society, em Barre/MA, EUA. Grande parte dos professores de meditação, americanos leigos, da tradição de Mahasi Sayadaw, estudaram com U Pandita nesse retiro ou em retiros subsequentes. As palestras do Dhamma desse retiro foram compiladas no livro <i>In This Very Life</i>. </p>

<p class=Normal>Nos anos seguintes U Pandita dirigiu vários retiros em vários países, incluindo, além dos Estados Unidos, a Malásia, o Nepal e a Austrália. </p>

<p class=Normal>Em 1991 ele deixou o Thathana Yeiktha e abriu o Centro de Meditação Panditarama em Rangoon. Na atualidade existem vários centros Panditarama, em Myanmar, Nepal, Austrália, Reino Unido e Estados Unidos. </p> 

<p class=Normal>Em 2003 o Ven. U Pandita dirigiu o retiro de meditação inaugural no Forest Refuge do Insight Meditation Society, em Barre/MA, EUA, retornando para um segundo retiro em 2005. </p> 

<p class=Normal>O Ven. U Pandita é o autor de <i>The State of Mind Called Beautiful</i>, <i>In This Very Life: The Liberation Teachings of the Buddha</i>, <i>Raindrops in hot summer: Words of advice from Sayadaw U Pandita</i>, <i>Way to the Happiness of Peace</i>. </p>

