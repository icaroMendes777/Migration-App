<p class=Normal><b>Nota do Tradutor:</b></p>
<p class=Normal>Os textos que apresento aqui traduzidos são quatro conferências dadas pelo Ven. Nyanatiloka em datas e locais diversos. Essas conferências deram origem ao livreto Fundamentals of Buddhism. Não nos enganemos com o nome livreto, o conteúdo está longe de ser no diminutivo. Trata-se, na minha opinião, de uma das melhores e mais ricas apresentações do Budismo que li até hoje. Os principais elementos doutrinais do Budismo Theravada são tratados de forma ao mesmo tempo profunda e acessível, não deixando margem para toda gama de equívocos acerca de Budismo que encontramos mesmo em textos bem intencionados hoje em dia. Meu desejo com esta tradução é que as pessoas tenham um primeiro contato com o Budismo que as ajude de fato a compreender os ensinamentos e mais importante, separar o que é Budismo do que não é, algo que não é tão fácil como aparenta em tempos de internet. Minha intenção era traduzir por completo e apresentar para publicação a alguma editora especializada interessada na idéia. Ainda considero uma possibilidade mas resolvi disponibilizar pelo Acesso ao Insight pois assim chega mais rápido às mãos das pessoas. Sinto que em nosso país não existem textos didáticos para os interessados em Budismo nem traduções publicadas do Cânone Pali. Espero que este pequeno esforço ajude a diminuir esta carência. Que possam todos se beneficiar.</p>
