<p class=Normal><b><a name=T2></a><a name=T21></a><span lang=PT-BR style='mso-ansi-
PT-BR'>O problema do bom e do mau</b><o:p></o:p></span></h3>

<p class=Normal>Como kamma
está diretamente relacionado com o bom e o mau, qualquer discussão sobre kamma tem
que incluir também uma discussão sobre o bom e o mau. Os padrões para definir o
bom e o mau, no entanto, não estão isentos de dificuldades. O que é<span
style='mso-spacerun:yes'>  </span>“bom” e porque é assim? O que é que chamamos
de “mau” e porque é assim? Essas dificuldades na verdade são um problema de
linguagem. Nos ensinamentos do Buda, que estão fundamentados em Pali, o
significado é muito mais claro, como será demonstrado a seguir. <o:p></o:p></span></p>

<p class=Normal>As palavras
“bom” e “mau” possuem significados bastante amplos e em particular a palavra
“bom,” usada com mais freqüência do que “mau.” Diz-se que uma pessoa virtuosa e
moral é boa; uma comida deliciosa é chamada de “boa” comida; um pedaço de
madeira que tem alguma utilidade pode ser chamado de um “bom” pedaço de
madeira. Além disso, algo que seja bom para uma pessoa pode não sê-lo para
muitas outras. Visto por um ângulo, uma certa coisa pode ser boa, mas, se vista
por outro ângulo pode não ser.<o:p></o:p></span></p>

<p class=Normal>Um
comportamento considerado bom em uma área, região ou sociedade, pode ser
considerado ruim em outra. Parece, por esses exemplos, que existe uma certa
divergência. Pode ser necessário considerar a palavra “bom” sob diferentes
pontos de vista, tal como bom com um sentido hedonista, bom com um sentido
artístico, bom com um sentido econômico e assim por diante. A razão para essa
divergência é uma questão de valores. As palavras “bom” e “mau” podem ser
usadas em diferentes sistemas de valores o que faz com que o seu significado
seja bastante amplo.<o:p></o:p></span></p>

<p class=Normal>No nosso
estudo sobre o bom e o mau, os seguintes pontos devem ser observados:<o:p></o:p></span></p>


<p class=Normal>(a) Nosso
estudo será feito sob a perspectiva da lei de kamma, assim estaremos usando os
termos especializados <i>kusala</i> e <i>akusala</i> ou hábil e inábil,
benéfico e prejudicial, que possuem um significado bastante preciso.<o:p></o:p></span></p>


<p class=Normal>(b) <i>Kusala</i>
e <i>akusala</i>, na ética Budista, são qualidades da lei de kamma, portanto
nosso estudo está ajustado a esse contexto e não a um conjunto de valores
sociais que com freqüência são referidos com as palavras “bom” e “mau.”<o:p></o:p></span></p>


<p class=Normal>(c) Como
mencionado no Primeiro Capítulo, a operação da lei de kamma está relacionada
com outras leis. Especialmente, no que diz respeito à vida interior do
indivíduo, <i>kammaniyama</i> interage com as leis psicológicas (<i>cittaniyama</i>),
enquanto que externamente ela está relacionada com a Preferência Social.<o:p></o:p></span></p>

<p class=Normal><b><a name=T22></a>O significado
de kusala e akusala<o:p></o:p></span></b></h3>

<p class=Normal>Embora <i>kusala</i>
e <i>akusala</i> sejam algumas vezes traduzidos como “bom” e “mau,” isso pode
levar ao erro. As coisas que são <i>kusala </i>nem sempre serão consideradas
boas, enquanto que algumas coisas podem ser <i>akusala</i> e ainda assim não serão
em geral consideradas como ruins. A<span style='mso-spacerun:yes'> 
</span>melancolia, preguiça e distração, por exemplo, embora sejam <i>akusala, </i>não
são sempre consideradas como “ruins.” Da mesma forma, algumas formas de <i>kusala,
</i>tal como a calma corporal e mental podem não ser prontamente tomadas como
parte do entendimento geral da palavra “bom.”<o:p></o:p></span></p>


<p class=Normal><i>Kusala</span></i><span
lang=PT-BR style='mso-ansi-e <i>akusala</i> são condições que
surgem na mente produzindo inicialmente<span style='mso-spacerun:yes'> 
</span>resultados na mente e a partir daí para as ações externas e
características físicas. Portanto, os significados de <i>kusala </i>e <i>akusala
</i>enfatizam o estado, o conteúdo e os eventos mentais como base deles.<o:p></o:p></span></p>

<p class=Normal><i>Kusala</span></i><span
lang=PT-BR style='mso-ansi-em geral pode ser interpretado como
“inteligente, hábil, satisfeito, benéfico, bom” ou “aquilo que elimina a
aflição.” <i>Akusala </i>é definido com os opostos como “não inteligente,”
“inábil”, “prejudicial” e assim por diante.<o:p></o:p></span></p>

<p class=Normal>A seguir
encontram-se quatro conotações de <i>kusala </i>derivadas dos Comentários:<o:p></o:p></span></p>

<p class=Normal>1. <b><i>Arogya</i></b>:
livre de enfermidades; uma mente que é saudável; estados mentais que contêm essas
condições ou fatores, que suportam a saúde mental e produzem uma mente
despreocupada e estável.<o:p></o:p></span></p>

<p class=Normal>2. <b><i>Anavajja</i></b>:
imaculado; fatores que fazem com que a mente esteja limpa e clara, não manchada
ou turva.<o:p></o:p></span></p>

<p class=Normal>3. <b><i>Kosalasambhuta</i></b>:
baseado na sabedoria ou inteligência; estados mentais que estão baseados no
conhecimento e compreensão da verdade. Suportado pelo ensinamento que afirma
que as condições <i>kusala</i> têm <i>yoniso-manasikara</i>, atenção com
sabedoria, como precursor.<o:p></o:p></span></p>

<p class=Normal>4. <b><i>Sukhavipaka</i></b>:
recompensado pelo bem-estar. <i>Kusala</i> é uma condição que produz o
contentamento. Quando as condições <i>kusala</i> surgem na mente, existe uma
sensação natural de bem-estar, sem necessidade de nenhuma influência
externa.Como quando alguém se sente forte e saudável (<i>aroga</i>), banhado e
limpo (<i>anavajja</i>), num lugar seguro e confortável (<i>kosalasambhuta</i>),
uma sensação de bem-estar surge de forma natural.<o:p></o:p></span></p>

<p class=Normal>O
significado de <i>akusala</i> deve ser compreendido de maneira exatamente
oposta àquela mencionada acima: ou seja, como a mente que não é saudável, que é
prejudicial, baseada na ignorância,<span style='mso-spacerun:yes'> 
</span>resultando em sofrimento. Em suma, se refere àquelas condições que fazem
com que a mente degenere tanto em termos de qualidade como eficiência, ao contrário
de <i>kusala</i> que promove a qualidade e eficiência da mente.<o:p></o:p></span></p>

<p class=Normal>Para
aclarar um pouco mais esses conceitos, é recomendável revisar a descrição,
encontrada nos Comentários, dos atributos de uma mente boa, aquela que é
saudável e isenta de problemas e depois considerar se as condições <i>kusala</i>
de fato induzem a mente a ser dessa forma e, se assim for, como fazem isso.
Poderemos então considerar se as condições <i>akusala</i> privam a mente de
tais estados e como fazem isso.<o:p></o:p></span></p>

<p class=Normal>Para
facilidade de referência, as várias características de uma mente boa, aquela
que é saudável e isenta de problemas, encontradas nos Comentários, podem ser
compiladas em grupos da seguinte forma:<o:p></o:p></span></p>

<p class=Normal>1. Firme:
decidida, estável, imóvel, sem distrações. <o:p></o:p></span></p>

<p class=Normal>2. Pura e
Limpa: sem manchas, imaculada, luminosa.<o:p></o:p></span></p>

<p class=Normal>3. Clara e
livre: sem restrições, livre, transcendente, ilimitada.<o:p></o:p></span></p>

<p class=Normal>4. Adequada
para o trabalho: flexível, leve, maleável, paciente.<o:p></o:p></span></p>

<p class=Normal>5. Calma e
satisfeita: relaxada, tranqüila, satisfeita.<o:p></o:p></span></p>

<p class=Normal>Tendo
analisado as qualidades de uma mente saudável, poderemos agora considerar as qualidades
que são conhecidas como <i>kusala</i> e <i>akusala</i>, verificando como afetam
a qualidade da mente. <o:p></o:p></span></p>

<p class=Normal>Alguns
exemplos de condições <i>kusala</i> são: <i>sati,</i> atenção plena ou memória,
a habilidade de manter a atenção em qualquer objeto ou tarefa na qual a mente
esteja engajada; <i>metta, </i>amor bondade ou boa vontade; não cobiça,
ausência de desejo e apego (incluindo pensamentos altruístas); sabedoria, clara
compreensão de como as coisas na verdade são; calma, tranqüilidade e paz; <i>kusalachanda,
</i>zelo ou contentamento com o bem; o desejo de compreender e agir de acordo
com a verdade; e contentamento pela boa fortuna dos outros.<o:p></o:p></span></p>

<p class=Normal>Quando
existe boa vontade, a mente está feliz, alegre e límpida naturalmente. Essa é
uma condição benéfica para a psique, suportando a qualidade e eficiência da
mente. A boa vontade portanto é <i>kusala. Sati </i>possibilita que a atenção
permaneça com o que quer que seja que a mente esteja envolvida ou engajada,
lembrando a ação adequada que deve ser tomada e ajudando a prevenir que
condições <i>akusala</i> surjam, permitindo assim que a mente trabalhe de forma
mais efetiva. <i>Sati </i>portanto é <i>kusala.</i><o:p></o:p></span></p>

<p class=Normal>Exemplos de
condições <i>akusala</i> são: desejo sensual; má vontade; preguiça e torpor;
inquietação e ansiedade; dúvida [<a href="#N7">7</a>], <a name=R7></a>raiva,
inveja e avareza.<o:p></o:p></span></p>

<p class=Normal>A inveja
faz com que a mente se torne malévola e opressiva, claramente prejudicando a
qualidade e a saúde da mente. Portanto ela é <i>akusala. </i>A raiva incita a
mente de tal modo que rapidamente afeta até mesmo a saúde do corpo e portanto é
claramente <i>akusala.</i> O desejo sensual confunde e obceca a mente. Isso
também é <i>akusala.</i><o:p></o:p></span></p>

<p class=Normal>Tendo
estabelecida a compreensão das palavras <i>kusala</i> e <i>akusala</i>, estamos
agora prontos para compreender o <i>kamma</i> bom e ruim, ou <i>kusala kamma</i>
e <i>akusala kamma</i>. Como já foi mencionado, a intenção é o cerne de <i>kamma.
</i>Portanto uma intenção que contenha condições <i>kusala</i> é hábil e uma
intenção que contenha condições <i>akusala</i> é inábil. Quando essas intenções
hábeis ou inábeis são convertidas em ações através do corpo, linguagem ou
mente, elas são conhecidas como <i>kamma</i> hábil ou inábil através do corpo,
linguagem e mente respectivamente, ou como alternativa, <i>kamma</i> corporal, <i>kamma</i>
verbal e <i>kamma</i> mental que são hábeis ou inábeis conforme for o caso.<o:p></o:p></span></p>

<p class=Normal><b><a name=T23></a>Kusala e
akusala como catalisadores mútuos</b><o:p></o:p></span></h3>

<p class=Normal>Um ato de
fé ou generosidade, a pureza moral ou<span style='mso-spacerun:yes'> 
</span>mesmo uma experiência de insight durante a meditação, todas condições <i>kusala</i>,
podem precipitar o surgimento da presunção, orgulho e arrogância. A presunção e
o orgulho são condições <i>akusala</i>. Esta situação é conhecida como “<i>kusala
</i>agindo como agente para <i>akusala.” </i>A prática da meditação pode levar
a estados de intensa concentração mental (<i>kusala</i>), que por seu lado
podem conduzir ao apego (<i>akusala</i>). O desenvolvimento de pensamentos de
amor bondade e benevolência com relação aos outros (<i>kusala</i>) pode, com a
presença de um objeto desejável, precipitar o surgimento da cobiça (<i>akusala</i>).
Esses são exemplos de kusala agindo como um agente para<span
style='mso-spacerun:yes'>  </span><i>akusala</i>.<o:p></o:p></span></p>

<p class=Normal>Algumas
vezes a prática da virtude ou da meditação (<i>kusala</i>) podem estar baseadas
no desejo de renascer no paraíso (<i>akusala</i>). O bom comportamento de uma
criança (<i>kusala</i>) pode estar baseado no desejo de se mostrar para os mais
velhos (<i>akusala</i>); o zelo de um estudante em aprender (<i>kusala</i>)
pode ser derivado da ambição (<i>akusala</i>); a raiva (<i>akusala</i>), vista
sob a perspectiva dos seus efeitos danosos, pode conduzir à atenção com
sabedoria e ao perdão (<i>kusala</i>); o medo da morte (<i>akusala</i>) pode
encorajar a introspecção (<i>kusala</i>): esses são exemplos de <i>akusala</i>
como um agente para <i>kusala</i>.<o:p></o:p></span></p>

<p class=Normal>Um exemplo:
os pais de um adolescente o advertem que as companhias dele exercem uma má
influência, mas ele não lhes dá atenção e é seduzido pelo vício das drogas. Ao
compreender a sua situação ele primeiro fica com raiva e deprimido e depois,
lembrando do alerta dos pais, ele se sente comovido pela compaixão deles (<i>akusala</i>
como agente para <i>kusala</i>), mas isso, por sua vez, apenas agrava a raiva
que sente por si mesmo (<i>kusala</i> como agente para <i>akusala</i>).<o:p></o:p></span></p>

<p class=Normal>Essas
mudanças de <i>kusala</i> para <i>akusala</i> ou <i>akusala</i> para <i>kusala</i>
ocorrem com tanta rapidez que a mente des-treinada raramente é capaz de
notá-las.<o:p></o:p></span></p>

<p class=Normal><b><a name=T24></a>Avaliando
kamma bom e ruim</b><o:p></o:p></span></h3>

<p class=Normal>Foi
mencionado que a lei de <i>kamma </i>possui uma relação bastante íntima com
ambas, as leis psicológicas e a preferência social. Essa afinidade pode criar
mal entendidos com facilidade. A lei de <i>kamma </i>está relacionada com as
leis psicológicas de forma tão próxima que parecem ser a mesma coisa, mas
existe uma clara linha divisória entre as duas, que é a intenção. Essa é a
essência e força motivadora da lei de <i>kamma </i>e é o que proporciona à lei
de <i>kamma </i>o seu nicho distinto entre os outros<span
style='mso-spacerun:yes'>  </span><i>niyama</i> ou leis. <i>Cittaniyama</i>,
por outro lado, governa todas as atividades mentais, incluindo as não
intencionais.<o:p></o:p></span></p>

<p class=Normal>A intenção
humana, através da lei de <i>kamma</i>, possui o seu próprio papel distinto dos
demais <i>niyama</i>, dando origem à ilusão de que os seres humanos são
independentes do mundo natural. A intenção precisa contar com a mecânica de <i>cittaniyama</i>
para poder funcionar e o processo de criar <i>kamma </i>precisa operar dentro
dos parâmetros de <i>cittaniyama</i>.<o:p></o:p></span></p>

<p class=Normal>Usando a
analogia de um homem pilotando um barco a motor, o “piloto” é a intenção, que é
a esfera de atividade da lei de <i>kamma,</i> enquanto que o motor do barco
pode ser comparado aos fatores mentais que são função de <i>cittaniyama</i>. O
piloto depende do motor do barco. No entanto, para que o “motor do barco” possa
conduzir o “barco,” isto é, para que a mente dirija a vida e o corpo para
qualquer direção, ela depende inteiramente do arbítrio do “piloto,” a intenção.
O piloto depende do barco e o utiliza, mas também toma a responsabilidade pelo
bem estar de ambos, barco e motor. Da mesma forma, a lei de <i>kamma </i>depende
de <i>cittaniyama </i>e a utiliza e também toma a responsabilidade pelo bem
estar da vida, incluindo ambos, o corpo e a mente.<o:p></o:p></span></p>

<p class=Normal>Não existe
tanta confusão sobre a relação entre a lei de <i>kamma</i> e <i>cittaniyama</i>,
principalmente porque essas são coisas pelas quais a pessoa comum não tem um
grande interesse. O tema que cria mais confusão é a relação entre a lei de <i>kamma
</i>e a Preferência Social e essa confusão gera ambigüidade com relação à
natureza do bom e do mau.<o:p></o:p></span></p>

<p class=Normal>Com
freqüência ouvimos as pessoas dizerem que o bom e o mau são invenções humanas
ou sociais. Uma ação numa sociedade, época ou lugar, pode ser considerada boa,
mas em outra época e lugar será considerada má. Algumas ações são aceitáveis
numa sociedade, mas não em outra. Por exemplo, algumas religiões ensinam que
matar animais para alimento não é mau, enquanto que outras ensinam que causar
dano aos seres de qualquer tipo nunca é bom. Algumas sociedades crêem que uma
criança deve mostrar respeito aos mais velhos e que discutir com eles são maus
modos, enquanto que outras crêem que o respeito não depende de idade e que as
pessoas devem ter o direito de expressar as suas opiniões.<o:p></o:p></span></p>

<p class=Normal>Dizer que o
bom e o mau são questões da preferência humana e decreto social é verdade até
certo ponto. Mesmo assim, o bom e o mau da Preferência Social não afetam ou
desordenam a mecânica da lei de <i>kamma </i>sob nenhum aspecto e não devem ser
confundidos com ela. “Bom” e “mau” como convenção social devem ser reconhecidos
como Preferência Social. Quanto a “bom” e “mau” ou de forma mais correta, <i>kusala</i>
e <i>akusala</i>, as qualidades da lei de <i>kamma</i>, estas devem ser
reconhecidas como atributos da lei de <i>kamma</i>. Muito embora ambas estejam
relacionadas, elas são de fato separadas e possuem distinções bastante claras.<o:p></o:p></span></p>

<p class=Normal>Aquilo que
é ao mesmo tempo a relação e o ponto de diferença entre essa lei da natureza e
a Preferência Social é a intenção ou volição. Quanto à forma como isso ocorre,
consideremos o seguinte.<o:p></o:p></span></p>

<p class=Normal>Em relação
à lei de <i>kamma</i>, as convenções da sociedade podem ser divididas em dois
tipos:<o:p></o:p></span></p>

<p class=Normal>1. <b>Aquelas
que não possuem relação direta com <i>kusala</i> e <i>akusala</i></b>.<o:p></o:p></span></p>

<p class=Normal>2. <b>Aquelas
que estão relacionadas com <i>kusala</i> e <i>akusala</i></b>.<o:p></o:p></span></p>

<p class=Normal><b>Aquelas
convenções que não possuem relação direta com <i>kusala</i> e <i>akusala</i></span></b> são os valores aceitos ou pactos
que são estabelecidos pela sociedade para uma função social específica, tal
como permitir que as pessoas vivam juntas em harmonia. Elas podem de fato ser
instrumentos para criar a harmonia social ou não. Elas podem de fato ser úteis
para a sociedade ou podem na verdade causar dano. Tudo depende se essas
convenções foram ou não estabelecidas com suficiente compreensão e sabedoria e se
a autoridade que as estabeleceu está agindo ou não com a intenção pura.<o:p></o:p></span></p>

<p class=Normal>Esses tipos
de convenções podem assumir várias formas, tais como tradições, costumes ou
leis. “Bom” e “mau” nesse contexto são estritamente assuntos de Preferência
Social. Elas podem mudar de vários modos, mas as suas mudanças não são função
da lei de <i>kamma </i>e não devem ser confundidas com ela. Se uma pessoa
desobedecer essas convenções e for punida pela sociedade, isso também é um
assunto da Preferência Social, não da lei de <i>kamma</i>.[<a href="#N8">8</a>]<o:p></o:p></span></p>

<p class=Normal><a name=R8></a><span lang=PT-BR style='mso-ansi-
PT-BR'>Consideremos agora uma área em que essas preferências sociais podem
coincidir com a lei de <i>kamma, </i>tal como quando um membro de uma sociedade
se recusa a obedecer uma das suas convenções ou a transgride.[<a href="#N9">9</a>]
<a name=R9></a>Ao agir assim, aquela pessoa estará agindo de acordo com certa
intenção. Essa intenção é o primeiro passo e portanto diz respeito à lei de <i>kamma.
</i>Em muitas sociedades haverá o esforço de identificar essa intenção para
averiguar a qualidade da ação. Isso novamente é um tema da Preferência Social,
indicando que aquela sociedade em particular sabe como empregar a lei de <i>kamma.</i>
Essa consideração da intenção pela sociedade não é no entanto, em si mesma,
função da lei de <i>kamma.</i> (Isto é, não é uma conclusão conhecida de
antemão pois nem sempre o comportamento ilegal<span style='mso-spacerun:yes'> 
</span>é punido. No entanto, quer as ações sejam punidas ou não elas são <i>kamma
</i>no sentido de que são ações volitivas e irão produzir resultados).<o:p></o:p></span></p>

<p class=Normal>Quanto ao
papel particular da lei de <i>kamma</i>, não obstante a sociedade investigar a
intenção ou não, ou mesmo se a sociedade estiver ciente da transgressão ou não,
a lei de <i>kamma</i><span style='mso-spacerun:yes'>  </span>funciona
imediatamente após a ocorrência da ação e o processo de fruição já terá sido
colocado em movimento.<o:p></o:p></span></p>

<p class=Normal>Colocando
de forma simples, o fator decisivo na lei de <i>kamma </i>é se a intenção é <i>kusala</i>
ou <i>akusala</i>. Na maioria dos casos, desobedecer alguma Preferência Social
pode ser dito que não constitui uma transgressão intencional somente quando a
sociedade concorda em abandonar ou modificar aquela convenção. Só então, não
haverá transgressão do pacto público. <o:p></o:p></span></p>

<p class=Normal>Isso pode
ser ilustrado com um exemplo simples. Suponha que duas pessoas decidam viver
juntas. Para que a vida delas, juntas, seja a mais tranqüila e conveniente
possível, elas concordam em estabelecer um conjunto de regras: embora trabalhem
em lugares diferentes e regressem do trabalho em horários diferentes, elas
decidem jantar juntas. Como seria impraticável que uma esperasse pela outra
indefinidamente, elas concordam que cada uma não deveria comer antes das sete
horas da noite. Dessas duas pessoas, uma gosta de gatos e não gosta de cães,
enquanto que a outra gosta de cães e não gosta de gatos. Para o bem estar
comum, elas concordam em não ter animais de estimação na casa.<o:p></o:p></span></p>

<p class=Normal>Tendo concordado
com essas regras, se alguma dessas duas pessoas agir em desacordo com elas,
haverá o caso de uma transgressão intencional e o <i>kamma </i>surgirá, bom ou
mal, de acordo com a intenção que a tenha instigado, muito embora jantar antes
das sete horas da noite ou ter animais de estimação na casa não seja em si
mesmo bom ou mal. Um outro casal poderá até estabelecer regras que sejam
diretamente opostas a essas. E na eventualidade de alguma dessas pessoas
considerar que as regras em vigor não mais tragam benefício, elas devem
discutir o assunto juntas e chegar a um novo acordo. Só então, uma não
conformidade intencional por parte de uma delas estaria livre de resultado <i>kammico.
</i>Essa é a distinção entre “bom” e “mau” e “certo” e “errado” como parte das
convenções sociais em transformação, ao contrário das propriedades imutáveis da
lei de <i>kamma - kusala </i>e <i>akusala.</i><o:p></o:p></span></p>

<p class=Normal><b>As
convenções sociais que estão relacionadas com <i>kusala</i> e <i>akusala</i></span></b><span
lang=PT-BR style='mso-ansi-da lei de <i>kamma</i> são aquelas
convenções que ou são hábeis, ou inábeis. A sociedade pode ou não estabelecer
essas regras com um claro entendimento de <i>kusala</i> e <i>akusala</i>, mas o
processo da lei de <i>kamma</i> segue adiante o seu curso natural sem levar
isso em consideração. Isso não muda de acordo com as convenções sociais.<o:p></o:p></span></p>

<p class=Normal>Por
exemplo, uma sociedade pode considerar aceitável tomar substâncias embriagantes
e drogas que viciam. As emoções extremas podem ser encorajadas e os cidadãos
podem ser estimulados a competir de forma agressiva para estimular o
crescimento econômico. Ou a crença geral pode ser que, matar as pessoas de
outras sociedades, ou, numa escala menor, matar animais, não é passível de
crítica.<o:p></o:p></span></p>

<p class=Normal>Esses são
exemplos nos quais o bom e mau da Preferência Social e <i>kusala</i> e <i>akusala</i>
divergem entre si: condições inábeis são preferidas do ponto de vista social e
o “bom” sob a perspectiva social é “mau” sob o ponto de vista da lei de <i>kamma</i>.
Visto sob a perspectiva social, essas convenções ou atitudes podem causar
ambos, resultados positivos e negativos. Por exemplo, embora a vida estressada
e marcada pela alta competitividade possa provocar uma alta taxa de suicídios,
uma incidência extraordinariamente alta de problemas mentais e sociais,
enfermidades cardíacas e assim por diante, uma sociedade com tal perfil poderá
experimentar rápido progresso material. Portanto, os problemas sociais com
freqüência podem ser rastreados até a lei de <i>kamma,</i><span
style='mso-bidi-font-style:italic'> nos</span> valores condenados e encorajados
pela sociedade.<o:p></o:p></span></p>

<p class=Normal>A
Preferência Social e a lei de <i>kamma </i>são separadas e distintas. Os frutos
de <i>kamma </i>surgem de acordo com a sua própria lei, independente de
qualquer convenção social com as quais tenha diferenças, tal como mencionado
acima. No entanto, como as convenções e a lei de <i>kamma </i>estão
relacionadas, praticar ações corretas sob a perspectiva da lei de <i>kamma, </i>isto
é, ações que são <i>kusala, </i>podem mesmo assim criar problemas no plano
social. Por exemplo, um abstêmio que viva numa sociedade que favoreça as
substâncias embriagantes irá receber os frutos de <i>kamma</i> ditados pela lei
de <i>kamma</i> – ele não experimenta a perda da saúde e da claridade mental
devido às substâncias embriagantes – mas no contexto da Preferência Social, em
contraste com a lei de <i>kamma, </i>ele poderá ser ridicularizado e
desprezado. E mesmo como parte da lei de <i>kamma </i>poderão surgir problemas
do seu antagonismo intencional a essa Preferência Social, sob a forma de
estresse mental, que poderá ser maior ou menor dependendo da sua sabedoria e
habilidade em se desapegar das reações sociais.<o:p></o:p></span></p>

<p class=Normal>Uma
sociedade progressista com administradores sábios utiliza a experiência
acumulada de prévias gerações ao estabelecer as convenções e leis da sociedade.
Essas se tornam o bom e o mau da Preferência Social e de forma ideal elas
deveriam estar correlacionadas com<span style='mso-spacerun:yes'>  </span><i>kusala</i>
e <i>akusala</i> de <i>kammaniyama</i>. A habilidade em estabelecer as
convenções sociais em conformidade com a lei de <i>kamma </i>parece ser uma
medida segura para determinar a verdadeira extensão do progresso ou civilização
de uma sociedade.<o:p></o:p></span></p>

<p class=Normal>Nesse
contexto, se for necessário avaliar qualquer convenção como boa ou má, é melhor
considerá-la sob dois aspectos. Primeiro, em termos da Preferência Social, ao
determinar se ela traz ou não um resultado benéfico para a sociedade. Segundo,
em termos da lei de <i>kamma, </i>determinando se é <i>kusala, </i>benéfica
para o bem estar mental.<o:p></o:p></span></p>

<p class=Normal>Algumas
convenções, embora sejam mantidas pelas sociedades por longos períodos de
tempo, na verdade não são úteis, mesmo sob o ponto de vista da Preferência
Social, sem falar sob o ponto de vista da lei de <i>kamma. </i>Tais convenções
devem ser abandonadas, o que pode exigir um ser excepcional, com o coração
puro, para apontar o seu defeito.<o:p></o:p></span></p>

<p class=Normal>No caso de
uma convenção que é vista como benéfica para a sociedade e para o progresso
humano, mas que não está em conformidade com <i>kusala </i>da lei de <i>kamma,</i>
tal como aquela que acentua o progresso material ao custo da qualidade de vida,
vale a pena considerar se as pessoas daquela sociedade não estão
desencaminhadas e equivocadas pensando que aquilo que é danoso seja benéfico.
Um costume verdadeiramente benéfico deve estar de acordo com ambos, a
Preferência Social e a lei de <i>kamma. </i>Em outras palavras, tem que ser
benéfico para ambos, o indivíduo e a sociedade como um todo e benéfico em ambos
os níveis, material e psíquico.<o:p></o:p></span></p>

<p class=Normal>Com relação
a isso podemos aprender com a situação da sociedade no presente. Os seres
humanos, acreditando que a abundância de posses materiais seja o caminho para a
verdadeira felicidade, aplicam a sua energia no desenvolvimento material. Os
efeitos danosos de muitos dos nossos esforços em obter o progresso material
apenas agora estão se tornando evidentes. Apesar da aparente prosperidade da
sociedade, nós temos criado muitos perigos físicos novos e os problemas sociais
e ambientais nos ameaçam numa escala global. Da mesma forma como o progresso
material não deve destruir o corpo físico, o progresso social não deve destruir
a clareza mental.<o:p></o:p></span></p>

<p class=Normal>O Buda
ofereceu um conjunto de reflexões sobre <i>kusala</i> e <i>akusala </i><span
style='mso-bidi-font-style:italic'>para avaliarmos no âmbito prático a natureza
do bom e do mau</span>, encorajando a reflexão sobre ambos o bom e o mau
interior (consciência) e os ensinamentos dos sábios (sendo que ambos são o
fundamento da vergonha e temor das transgressões). [<a href="#N10">10</a>] <a
name=R10></a>Em terceiro lugar, ele recomendava a reflexão sobre os frutos das
ações, tanto sob a perspectiva individual como social. Como a natureza de <i>kusala
</i>e <i>akusala </i>pode nem sempre ser evidente, o Buda nos<span
style='mso-spacerun:yes'>  </span>aconselha a tomar como base os ensinamentos
religiosos e éticos, e, se tais ensinamentos não forem claros o suficiente, a
analisar o resultado das ações, mesmo que seja apenas sob a perspectiva social.<o:p></o:p></span></p>

<p class=Normal>Para a
maioria das pessoas, essas três bases para a reflexão ( isto é, sob o ponto de
vista do indivíduo, da sociedade e dos ensinamentos dos sábios) podem ser
usadas para avaliar o comportamento em inúmeras situações distintas,
assegurando que as suas ações sejam tão circunspectas quanto possível. <o:p></o:p></span></p>

<p class=Normal>Portanto,
os critérios para avaliar o bom e o mau são: no contexto de se uma ação é <i>kamma</i>
ou não, tomar a intenção como fator decisivo; e no contexto de se aquele <i>kamma
</i>é bom ou ruim, considerar o assunto de acordo com os seguintes princípios:<o:p></o:p></span></p>

<p class=Normal><b><a name=T25></a>Fatores
Primários</b><o:p></o:p></span></h3>

<p class=Normal > 1. Investigar a origem das ações: se as intenções das quais elas
surgiram provêm de uma das raízes hábeis de não-cobiça, não-aversão ou
não-delusão ou de uma das raízes inábeis da cobiça, aversão ou delusão.<o:p></o:p></span></p>

<p class=Normal>2.
Investigar os efeitos na psique ou bem-estar mental das ações: se elas fazem
com que a mente fique límpida, calma e saudável; se elas promovem ou inibem a
qualidade mental; se elas encorajam o surgimento de condições hábeis e a
redução de condições inábeis ou vice-versa.<o:p></o:p></span></p>

<p class=Normal><b><a name=T26></a>Fatores
Secundários</b><o:p></o:p></span></h3>

<p class=Normal>1.
Considerar se as ações são passíveis de censura pela própria pessoa ou não.<o:p></o:p></span></p>

<p class=Normal>2.
Considerar a qualidade das ações sob a perspectiva dos ensinamentos dos sábios.<o:p></o:p></span></p>

<p class=Normal>3.
Considerar os resultados das ações:<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> Em
relação a si mesmo</p>

<p class=Normal style='margin-left:.75in'> Em
relação aos outros</p>

<p class=Normal>É possível
classificar esses padrões de forma diferente, mas antes devemos esclarecer dois
pontos. Primeiro, analisar as ações como hábeis ou inábeis em relação às suas
raízes ou em si mesmas é em essência a mesma coisa. Segundo, com relação à
aprovação ou censura pelos sábios, podemos dizer que tais opiniões sábias estão
em geral preservadas nas religiões, convenções e leis. Embora essas convenções
nem sempre sejam sábias, e sendo assim, uma prática que conflite com elas não
será necessariamente inábil, no entanto,<span style='mso-spacerun:yes'> 
</span>pode ser dito que tais casos são na verdade a exceção e não a regra. <o:p></o:p></span></p>

<p class=Normal>Estamos agora
prontos para resumir os nossos padrões de bom e mau, ou <i>kamma </i>bom e
ruim, ambos estritamente de acordo com a lei de <i>kamma </i>e também em
relação à Preferência Social, respectivamente sob um ponto de vista moral
intrínseco e de acordo com a prescrição social.<o:p></o:p></span></p>

<p class=Normal>1. Em
relação ao benefício direto ou dano: essas ações são em si mesmas benéficas?
Elas contribuem para a qualidade de vida? Elas fazem com que as condições <i>kusala
</i>e <i>akusala </i>aumentem ou diminuam?<o:p></o:p></span></p>

<p class=Normal>2. Em
relação às conseqüências benéficas ou prejudiciais: os efeitos dessas ações são
prejudiciais ou benéficos para si mesmo?<o:p></o:p></span></p>

<p class=Normal>3. Em
relação ao benefício ou prejuízo para a sociedade: essas ações prejudicam ou
beneficiam os outros?<o:p></o:p></span></p>

<p class=Normal>4. Em
relação à consciência, a capacidade humana reflexiva natural: essas ações serão
passíveis de censura pela própria pessoa ou não?<o:p></o:p></span></p>

<p class=Normal>5. Em
relação aos padrões sociais: qual é a posição dessas ações em relação àquelas
convenções religiosas, tradições, instituições sociais e leis que estão
baseadas na reflexão com sabedoria (ao contrário daquelas que são meras crenças
supersticiosas ou equivocadas)?<o:p></o:p></span></p>

<p class=Normal>Antes de
tratar da questão dos resultados de <i>kamma </i>no próximo capítulo, seria
pertinente considerar alguns dos pontos descritos acima à luz do Cânone em
Pali.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> “O que são condições hábeis (<i>kusala</i>)? Elas são as três raízes da
habilidade – não-cobiça, não-aversão e não-delusão; sensações, percepções,
formações mentais e consciência, que contêm essas raízes da habilidade; kamma
corporal, kamma verbal e kamma mental, que possuem essas raízes como base:
essas são as condições hábeis.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> “O que são condições inábeis (<i>akusala</i>) Elas são as três raízes da
inabilidade – cobiça, aversão e delusão – e todas as contaminações que delas
surgem; sensações, percepções, formações mentais e consciência, que contêm
essas raízes da inabilidade; kamma corporal, kamma verbal e kamma mental, que
possuem essas raízes como base: essas são as condições inábeis.”[Dhp 181]<o:p></o:p></span></p>

<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-PT-BR'>* * *<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> “Existem dois tipos de perigo, o perigo visível e o perigo velado.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> “Quais são os ‘perigos visíveis’?
Essas são coisas tais como leões, tigres, panteras, ursos, leopardos, lobos …
bandidos … enfermidades do olho, enfermidades do ouvido, enfermidades do nariz
... frio, calor, fome, sede, defecar, urinar, contato com moscas,
mosquitos, vento, sol e animais rastejantes: esses são chamados os ‘perigos
visíveis.’</span></p>


<p class=Normal style='margin-left:.75in'> “Quais são os ‘perigos velados’? Eles são as ações prejudiciais
corporais, ações prejudiciais verbais, ações prejudiciais mentais; os
obstáculos do desejo sensual, má vontade, preguiça e torpor, inquietação e
ansiedade, dúvida; cobiça, aversão e delusão; raiva, vingança, malícia,
arrogância, inveja, avareza, fraude, ostentação, teimosia, disputas, orgulho,
desprezo, ilusão, negligência; as contaminações, os maus hábitos; confusão;
luxúria; agitação; todos os pensamentos que sejam inábeis: esses são os
‘perigos velados.’<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> “Eles são chamados de ‘perigos’ por qual razão? Eles são chamados de
perigos porque eles subjugam, causam o declínio, nesse aspecto eles são um refúgio.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> “Porque eles são chamados de perigos que subjugam? Porque esses perigos
suprimem, constrangem, sujeitam, oprimem, molestam e esmagam...<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> “Porque eles são chamados de perigos que causam o declínio? Porque esses
perigos produzem o declínio das condições hábeis...<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> “Porque eles são chamados de perigos que são um refúgio? Porque as
condições baixas, inábeis nascem dessas coisas e tomam refúgio nelas, tal como
um animal que vive num buraco toma refúgio no buraco, um animal aquático toma
refúgio na água ou um animal que vive nas árvores toma refúgio nas
árvores...”[Nd 112, 360, 467; Nd 2199]<o:p></o:p></span></p>


<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-PT-BR'>* * *<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> “Cobiça, raiva, e delusão,
surgem do próprio interior,
machucam quem tem a mente má
tal como a própria flor destrói o bambu.” [SN.I.70,98] (<a href="../sutta/SNIII.2.php">SN III.2</a>)<o:p></o:p></span></p>

<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-PT-BR'>* * *<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> “Grande rei, há três coisas que quando surgem numa pessoa surgem para o seu dano, sofrimento e desconforto. Quais três? A cobiça, a raiva, e a delusão. Essas são as três coisas que quando surgem numa pessoa surgem para o seu dano, sofrimento e desconforto.” [SN.I.98] (<a href="../sutta/SNIII.2.php">SN III.2</a>)<o:p></o:p></span></p>



<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-PT-BR'>* * *<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> “Bhikkhus, há essas três causas para a origem de kamma. Quais três? A cobiça é uma causa para a origem de kamma, a raiva é uma causa para a origem de kamma, e a delusão é uma causa para a origem de kamma.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>(1) "Qualquer kamma criado através da cobiça, nascido da cobiça, causado pela cobiça, originado da cobiça, esse kamma é prejudicial e repreensível e resulta no sofrimento. Esse kamma conduz ao surgimento de kamma, não à cessação de kamma.</p>

<p class=Normal style='margin-left:.75in'>(2) "Qualquer kamma criado através da raiva ... (3) Qualquer kamma criado criado através da delusão, nascido da delusão, causado pela delusão, originado da delusão, esse kamma é prejudicial e repreensível e resulta no sofrimento. Esse kamma conduz ao surgimento de kamma, não à cessação de kamma.</p>

<p class=Normal style='margin-left:.75in'>“Bhikkhus, há essas outras três causas para a origem de kamma. Quais três? A não-cobiça é uma causa para a origem de kamma, a não-raiva é uma causa para a origem de kamma, e a não-delusão é uma causa para a origem de kamma.</p>

<p class=Normal style='margin-left:.75in'> (1) "Qualquer kamma criado criado através da não-cobiça, nascido da não-cobiça, causado pela não-cobiça, originado da não-cobiça, esse kamma é benéfico e irrepreensível e resulta na felicidade. Esse kamma conduz à cessação de kamma, não ao surgimento de kamma. </p>

<p class=Normal style='margin-left:.75in'> (2) "Qualquer kamma criado através da não-raiva ... (3) Qualquer kamma criado através da não-delusão, nascido da não-delusão, causado pela não-delusão, originado da não-delusão, esse kamma é benéfico e irrepreensível e resulta na felicidade. Esse kamma conduz à cessação de kamma, não ao surgimento de kamma. [AN.I.263] (<a href="../sutta/ANIII.111.php">AN III.111</a>)<o:p></o:p></span></p>



<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-PT-BR'>* * *<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> 
“Claro que vocês estão confusos Kalamas. Claro que vocês têm dúvidas.
Porque a dúvida surge com relação a qualquer assunto que cause a perplexidade.
Dessa forma, Kalamas, nesse caso não se deixem levar pelos relatos, pelas
tradições, pelos rumores, por aquilo que está nas escrituras, pela razão, pela inferência, pela analogia, pela competência (ou confiabilidade) de alguém, por respeito por alguém, ou pelo pensamento, ‘Este contemplativo
é o nosso mestre.’ Quando vocês souberem por vocês mesmos que, ‘Essas qualidades
são inábeis; essas qualidades são culpáveis; essas qualidades são passíveis de
crítica pelos sábios; essas qualidades quando postas em prática conduzem ao mal
e ao sofrimento’ - então vocês devem abandoná-las.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'> 
“O que vocês pensam Kalamas? Quando a cobiça surge em uma pessoa, ela
surge para o bem ou para o mal?”<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> 
“Para o mal, senhor.”<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> 
“E essa pessoa que está tomada pela cobiça, sua mente obcecada com a
cobiça, mata seres vivos, toma o que não lhe é dado, busca a mulher do próximo,
mente e induz outros a fazerem o mesmo, tudo o que resulta é dano e sofrimento
por um longo tempo.”<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
“Sim, senhor”<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
“E o que vocês pensam, Kalamas? Quando a aversão surge em uma pessoa,
ela surge para o bem ou para o mal?”<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> 
“Para o mal, senhor.”<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
“E essa pessoa tomada pela aversão, sua mente obcecada pela aversão,
mata seres vivos, toma o que não lhe é dado, busca a mulher do próximo, mente, e
induz outros a fazerem o mesmo, tudo o que resulta é dano e sofrimento por um
longo tempo.”<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> 
“Sim, senhor.”<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> 
“E o que vocês pensam, Kalamas? Quando a delusão surge em uma pessoa,
ela surge para o bem ou para o mal?”<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> 
“Para o mal, senhor.”<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> 
“E essa pessoa tomada pela delusão, sua mente obcecada pela delusão,
mata seres vivos, toma o que não lhe é dado, busca a mulher do próximo, mente,
e induz outros a fazerem o mesmo, tudo o que resulta é dano e sofrimento por um
longo tempo.”<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
“Sim, senhor.”<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
“Então Kalamas o que vocês pensam. Essa qualidades são hábeis ou
inábeis?”<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> 
“Inábeis, senhor.”<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
“Culpáveis ou isentas de culpa?”<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> 
“Culpáveis, senhor.”<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
“Criticadas pelos sábios ou elogiadas pelos sábios?”<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
“Criticadas pelos sábios, senhor.”<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
“Quando postas em prática elas conduzem ao mal e ao sofrimento ou não?”<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
“Quando postas em prática elas conduzem ao mal e ao sofrimento. Assim é
como as vemos.”<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> 
“Então como eu disse, Kalamas: ‘Não se deixem levar pelos relatos, pelas
tradições, pelos rumores, por aquilo que está nas escrituras, pela razão, pela inferência, 
pela analogia, pela competência (ou confiabilidade) de alguém, por respeito por alguém, ou pelo pensamento, “Este contemplativo
é o nosso mestre.” Quando vocês souberem por vocês mesmos que, “Essas qualidades
são inábeis; essas qualidades são culpáveis; essas qualidades são criticáveis
pelos sábios; essas qualidades quando postas em prática conduzem ao mal e ao
sofrimento” - então vocês devem abandoná-las.’ Assim foi dito.”[AN.I.189] (<a
href="../sutta/ANIII.65.php">AN.III.65</a>)</span><span lang=PT-BR
style='font-size:10.0pt;mso-bidi-font-size:12.0pt;mso-ansi-PT-BR'><o:p></o:p></span></p>

<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-PT-BR'>* * *<o:p></o:p></span></p>

<p class=Normal>O trecho a
seguir foi extraído de uma conversa entre o Rei Pasenadi de Kosala e o
Venerável Ananda. Trata-se de uma série de perguntas e respostas relacionadas
com a natureza do bom e do mau, onde pode ser observado que o Venerável Ananda
emprega todos os padrões mencionados acima. <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
Rei: Venerável Senhor, Quando pessoas tolas, não inteligentes, falam
elogiando ou criticando os outros sem fazer uma consideração cuidadosa, eu não
tomo as palavras delas com seriedade. Quanto aos expertos, os sábios e sagazes,
que fazem considerações cuidadosas antes de elogiar ou criticar, eu dou
importância às palavras deles. Agora, venerável Ananda, que tipo de ação
corporal é censurada por contemplativos e brâmanes sábios? <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
Ananda: Qualquer ação corporal ... verbal ... mental que seja
prejudicial, Majestade.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
Rei: Que tipo de ação<span style='mso-spacerun:yes'>  </span>corporal
... verbal ... mental é prejudicial?<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> 
Ananda: Qualquer ação corporal ... verbal ... mental que seja culpável.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
Rei: Que tipo de ação<span style='mso-spacerun:yes'>  </span>corporal
... verbal ... mental é culpável?<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> 
Ananda: Qualquer ação corporal ... verbal ... mental que traga aflição.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> 
Rei: Que tipo de ação<span style='mso-spacerun:yes'>  </span>corporal
... verbal ... mental traz aflição?<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> 
Ananda: Qualquer ação corporal ... verbal ... mental que tenha
resultados dolorosos.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> 
Rei: Que tipo de ação corporal ... verbal ... mental tem resultados
dolorosos?<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
Ananda: Qualquer ação corporal ... verbal ... mental que conduza à
própria aflição, ou à aflição dos outros, ou à aflição de ambos, e por conta do
qual os estados prejudiciais aumentam e os estados benéficos diminuem; esse
tipo de ação corporal ... verbal ... mental é censurada por contemplativos e
brâmanes sábios, grande rei.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> 
Em seguida o Venerável Ananda respondeu as perguntas do Rei sobre as
condições hábeis da mesma forma, resumindo da seguinte forma:<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> 
“Qualquer ação corporal ... verbal ... mental que que não conduza à
própria aflição, ou à aflição dos outros, ou à aflição de ambos, e por conta do
qual os estados prejudiciais diminuem e os estados benéficos aumentam. Esse
tipo de ação corporal ... verbal ... mental não é censurada por contemplativos
e brâmanes sábios, grande rei.” [M.II.114] (<a href="../sutta/MN88.php">MN88</a>)<o:p></o:p></span></p>


<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-PT-BR'>* * *<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> 
“Uma pessoa apaixonada, a sua mente atada, subjugada pela paixão ... pela raiva ... pela delusão, não compreende, como na verdade é, 
aquilo que lhe traz benefício, aquilo que traz benefício para os outros, aquilo que traz benefício 
para ambos. Mas tendo abandonado a paixão ... a raiva ... a delusão, ela compreende, como na verdade é, aquilo que lhe traz 
benefício, aquilo que traz benefício para os outros, aquilo que traz benefício para ambos.” [AN.I.216] (<a href="../sutta/ANIII.71.php">AN III.71</a>)<o:p></o:p></span></p>

<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-PT-BR'>* * *<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
“Igual ao leite, é a ação prejudicial,
que tão lentamente azeda.
Mas latente permanece no tolo,
como as brasas cobertas por cinzas.” [<a href="../dhp/dhp5.php">Dh 71</a>]<o:p></o:p></span></p>

<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-PT-BR'>* * *<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> 
“Quem substitui as ações ruins
com ações benéficas
ilumina o mundo
como a lua liberta das nuvens.”
[<a href="../dhp/dhp13.php">Dh 173</a>]<o:p></o:p></span></p>

<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-PT-BR'>* * *<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
 “As ações meritórias praticadas – esse é o amigo na vida futura.” [SN.I.37] (<a href="../sutta/SNI.53.php">SN I.53</a>)<o:p></o:p></span></p>

<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-PT-BR'>* * *<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> 
"Ananda, eu tenho declarado definitivamente que os atos de conduta corporal imprópria, conduta verbal imprópria, e conduta mental imprópria não devem ser praticados porque agindo assim é de se esperar este perigo: a pessoa culpa a si mesma; o sábio, tendo investigado, a censura; ela ganha má reputação; a pessoa morre confusa; e com a dissolução do corpo, após a morte, ela renasce no plano da miséria, em um destino ruim, nos mundos inferiores, no inferno …<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> 
"Ananda, eu tenho declarado definitivamente que os atos de conduta corporal apropriada, conduta verbal apropriada, e conduta mental apropriada devem ser praticados porque agindo assim é de se esperar este benefício: a pessoa não culpa a si mesma; o sábio, tendo investigado, a elogia; ela ganha boa reputação; a pessoa morre sem estar confusa; e com a dissolução do corpo, após a morte, ela renasce num destino feliz, no paraíso … (<a href="../sutta/ANII.18.php">AN II.18</a>)<o:p></o:p></span></p>

<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-PT-BR'>* * *<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'> 
"Abandonem o que não é hábil, bhikkhus. É possível abandonar o que não é hábil. Se não fosse possível abandonar o que não é hábil, eu não lhes diria ' Abandonem o que não é hábil.' Porém porque é possível abandonar o que não é hábil, eu lhes digo, 'Abandonem o que não é hábil.' Se o abandono do que não é hábil conduzisse ao que é prejudicial e doloroso, eu não diria para vocês, 'Abandonem o que não é hábil.' Mas porque o abandono do que não é hábil conduz ao que é benéfico e prazeroso, eu lhes digo, 'Abandonem o que não é hábil.'<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> 
"Desenvolvam o que é hábil, bhikkhus. É possível desenvolver o que é hábil. 
Se não fosse possível desenvolver o que é hábil, eu não lhes diria 'Desenvolvam o que é hábil.' 
Porém porque é possível desenvolver o que é hábil, eu lhes digo, 'Desenvolvam o que é hábil.' 
Se o desenvolvimento do que é hábil conduzisse ao que é prejudicial e doloroso, eu não diria para vocês, 
'Desenvolvam o que é hábil.' Mas porque o desenvolvimento do que é hábil conduz ao que é benéfico e prazeroso, 
eu lhes digo, 'Desenvolvam o que é hábil.'" [AN.I.58] (<a href="../sutta/ANII.19.php">AN II.19</a>)<o:p></o:p></span></p>

<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-PT-BR'>* * *<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> 
“Bhikkhus, há coisas para serem abandonadas através do corpo, não através da linguagem. Há coisas para serem abandonadas através da linguagem, não através do corpo. Há coisas para serem abandonadas nem através do corpo, nem através da linguagem mas repetidamente vendo através da sabedoria.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
“Quais, bhikkhus, são as coisas para serem abandonadas através do corpo, não através da linguagem? Aqui, um bhikkhu praticou algum ato prejudicial com o corpo. Os seus sábios companheiros na vida santa o investigam e dizem: 'Você cometeu um ato prejudicial com o corpo. Seria realmente muito bom se você abandonasse a conduta corporal imprópria e desenvolvesse a conduta corporal apropriada,' Tendo os seus sábios companheiros monges investigado e conversado com ele, ele abandona a conduta corporal imprópria e desenvolve a conduta corporal apropriada. Essas são chamadas as coisas para serem abandonadas através do corpo, não através da linguagem.<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'> 
“Quais são as coisas para serem abandonadas através da linguagem, não através do corpo? Aqui, um bhikkhu praticou algum ato prejudicial com a linguagem. Os seus sábios companheiros na vida santa o investigam e dizem: 'Você cometeu um ato prejudicial com a linguagem. Seria realmente muito bom se você abandonasse a conduta verbal imprópria e desenvolvesse a conduta verbal apropriada,' Tendo os seus sábios companheiros monges investigado e conversado com ele, ele abandona a conduta verbal imprópria e desenvolve a conduta verbal apropriada. Essas são chamadas as coisas para serem abandonadas através da linguagem, não através do corpo. <o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'> 
“Quais são as coisas para serem abandonadas nem através do corpo, nem através da linguagem mas repetidamente vendo através da sabedoria? A cobiça não é abandonada nem através do corpo, nem através da linguagem, mas repetidamente vendo através da sabedoria. A raiva ... a delusão ... o ódio ... a hostilidade ... a difamação ... a insolência ... a avareza não é abandonada nem através do corpo, nem através da linguagem, mas repetidamente vendo através da sabedoria.”[AN.V.39] ((<a href="../sutta/ANX.23.php">AN X.23</a>)<o:p></o:p></span></p>
