<p class=Normal><b>Os resultados da generosidade que você prática o acompanha em qualquer plano que você renasça: </b></p>
<p class=Normal>&nbsp;</p>

<p class=Normal><a name=R1></a> Certa ocasião o Abençoado estava em Savathi, no Bosque de Jeta, no Parque de Anathapindika. Então a Princesa Sumana,  acompanhada por quinhentas carruagens e quinhentas acompanhantes da corte foi até o Abençoado e depois de cumprimentá-lo ela sentou a um lado e disse para o Abençoado: </p>

