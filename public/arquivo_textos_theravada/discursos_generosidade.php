<p class=Normal><b>Cinco coisas que uma pessoa generosa dá, e cinco coisas que ela recebe: </b></p>
<p class=Normal>&nbsp;</p>

<p class=Normal> “Ao dar uma refeição, o doador dá cinco coisas a quem recebe. Quais cinco? Ele ou ela dá vida, beleza, felicidade, força e discernimento. Tendo dado vida, ele ou ela tem uma cota em longa vida, seja humana ou divina. Tendo dado beleza, ele ou ela tem uma cota em beleza, seja humana ou divina. Tendo dado felicidade, ele ou ela tem uma cota em felicidade, seja humana ou divina. Tendo dado força, ele ou ela tem uma cota em força, seja humana ou divina. Tendo dado discernimento, ele ou ela tem uma cota em discernimento, seja humano ou divino. Ao dar uma refeição, o doador dá estas cinco coisas a quem recebe.”</p>
