<p class=Normal><b>Não se arrependa da generosidade que você pratica:</b></p>
<p class=Normal>&nbsp;</p> 
<p class=Normal> Em Savatthi. Então o rei Pasenadi foi até o Abençoado e depois de cumprimentá-lo sentou a um lado. O Abençoado então disse o seguinte: </p>

<p class=Normal> "Grande rei, de onde você está vindo no meio do dia?" </p>

<p class=Normal> "Aqui, venerável senhor, um banqueiro em Savatthi morreu, eu venho depois de transportar a sua riqueza para o palácio visto que ele morreu sem deixar testamento." </p>

<p class=Normal> "Assim é, grande rei, assim é grande rei. Uma vez no passado um banqueiro ofereceu comida esmolada para um paccekabuddha chamado Tagarasikhi. Tendo dito, 'Dê comida para o contemplativo,' ele levantou do seu assento e partiu. Mas depois de ter dado ele se arrependeu, pensando: 'Teria sido melhor se os escravos ou trabalhadores tivessem comido aquilo!' Além disso ele matou o único filho do seu irmão para herdar a sua fortuna. </p>

<p class=Normal> "Visto que o banqueiro ofereceu comida ao paccekabuddha Tagarasikhi, como resultado desse kamma ele renasceu sete vezes num destino feliz. Como resultado desse kamma ele obteve por sete vezes a posição de banqueiro aqui mesmo em Savatthi. Mas como mais tarde ele se arrependeu da oferenda, como resultado residual desse kamma a sua mente não se inclinava para o prazer da boa comida, das boas roupas e dos bons veículos, nem para o prazer derivado dos cinco elementos do prazer sensual. E visto que aquele banqueiro matou o único filho do seu irmão para herdar a sua fortuna, como resultado desse kamma ele sofreu no inferno por muitas centenas de anos, por muitos milhares de anos. Como resultado residual desse kamma ele deixou para o tesouro real sete fortunas. </p>

<p class=Normal> "O antigo mérito desse banqueiro está completamente exaurido e ele não acumulou novos méritos. Assim hoje, grande rei, esse banqueiro está sendo assado no grande inferno Roruva." </p>

<p class=Normal> "Então, venerável senhor, esse banqueiro renasceu no grande inferno Roruva?" </p>

<p class=Normal> "Sim grande rei, esse banqueiro renasceu no grande inferno Roruva." </p>

<p class=Normal style='margin-left:.75in'>"Grãos, riqueza, ouro, e prata, <br>
ou quaisquer outras posses, <br>
escravos, trabalhadores, mensageiros, <br>
todos aqueles que vivem como dependentes: <br>
sem levar nada consigo ele tem que partir, <br>
tudo tem que ser deixado para trás. </p>

<p class=Normal style='margin-left:.75in'>"Mas aquilo que foi feito através do corpo, <br>
linguagem ou mente: <br>
isso deveras lhe pertence, <br>
isso ele leva ao partir; <br>
isso é aquilo que o segue<br>
tal como uma sombra que nunca parte. </p>

<p class=Normal style='margin-left:.75in'>"Portanto faça o bem<br>
como uma coleção para a vida futura. <br>
Méritos é aquilo que sustenta os seres vivos<br>
ao surgirem no outro mundo." </p>

