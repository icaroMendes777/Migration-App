<p class=Normal><b>Quais as intenções por trás de se dar um presente, e quais os resultados:</b></p>
<p class=Normal>&nbsp;</p>
 
<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Ouvi que em
certa ocasião o Abençoado estava em Campa, às margens do lago Gaggara. Então um
grande grupo de discípulos leigos de Campa foram até o Venerável Sariputta e,
quando chegaram, depois de cumprimentá-lo, sentaram a um lado e disseram: <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Já faz muito tempo,
venerável senhor, desde que tivemos a oportunidade de ouvir um discurso do
Dhamma na presença do Abençoado. Seria bom se pudéssemos ouvir um discurso do Dhamma
na presença do Abençoado.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Então nesse caso,
meus amigos, retornem no próximo dia de Uposatha, e talvez vocês possam ouvir
um discurso do Dhamma na presença do Abençoado.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Assim seja, venerável
senhor,&quot; os discípulos leigos de Campa disseram para o Venerável Sariputta.
Levantando-se dos seus assentos, e depois de homenageá-lo, mantendo-o à sua
direita, partiram. Então no próximo dia de Uposatha, os discípulos leigos de
Campa foram até o Venerável Sariputta e, tendo chegado, depois de cumprimentá-lo,
ficaram em pé a um lado. Então o Venerável Sariputta, juntamente com os discípulos
leigos de Campa, foram até o Abençoado e chegando, depois de cumprimentá-lo,
sentaram a um lado e o Venerável Sariputta disse para o Abençoado: <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Pode haver o caso em
que uma pessoa dá uma oferenda de um certo tipo e ela não produz grandes frutos e
grandes benefícios, enquanto que outra pessoa dá uma oferenda do mesmo tipo e ela
produz grandes frutos e grandes benefícios?&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Sim, Sariputta, há o
caso em que uma pessoa dá uma oferenda de um certo tipo e ela não produz grandes
frutos e grandes benefícios, enquanto que outra pessoa dá uma oferenda do mesmo
tipo e ela produz grandes frutos e grandes benefícios.&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Senhor, qual é a
causa, qual é a razão, porque uma pessoa dá uma oferenda de um certo tipo e ela
não produz grandes frutos e grandes benefícios, enquanto que outra pessoa dá
uma oferenda do mesmo tipo e ela produz grandes frutos e grandes
benefícios?&quot; <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Sariputta, há o caso
em que a pessoa dá uma oferenda buscando seu próprio benefício, com sua mente apegada
[à recompensa], pensando em acumulá-la para si mesma [com o pensamento], 'Eu a
desfrutarei após a morte.' Ela dá uma oferenda - comida, bebida, roupas, um
veículo, um ornamento, perfume e ungüento, roupas de cama, moradia, uma
lamparina - para um brâmane ou um contemplativo. O que você pensa , Sariputta?
Pode uma pessoa dar uma oferenda como essa?&quot;<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Sim, senhor.&quot;<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Tendo dado essa
oferenda buscando seu próprio benefício, com sua mente apegada [à recompensa],
pensando em acumulá-la para si mesma [com o pensamento], 'Eu a desfrutarei após
a morte.' - na dissolução do corpo, após a morte, ela renasce no mundo dos devas dos
Quatro Grandes Reis. Então tendo esgotado aquela ação, aquele poder, aquele
status, aquela soberania, ela retorna, voltando a este mundo. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Então há o caso em
que a pessoa dá uma oferenda sem buscar seu próprio benefício, sem a mente
apegada [à recompensa], não pensando em acumulá-la para si mesma nem [com o
pensamento], 'Eu a desfrutarei após a morte.' Ao invés disso, ela dá uma oferenda
com o pensamento ‘A generosidade é boa.’ Ela dá uma oferenda - comida, bebida,
roupas, um veículo, um ornamento, perfume e ungüento, roupas de cama, moradia,
uma lamparina- para um brâmane ou um contemplativo. O que você pensa ,
Sariputta? Pode uma pessoa dar uma oferenda como essa?&quot;<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Sim, senhor.&quot;<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Tendo dado essa
oferenda com o pensamento, ‘A generosidade é boa,’ na dissolução do corpo, após a
morte, ela renasce no mundo dos devas do Tavatimsa. Então tendo
esgotado aquela ação, aquele poder, aquele status, aquela soberania, ela
retorna, voltando a este mundo. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Ou, ao invés de
pensar ‘A generosidade é boa’ ela dá a oferenda com o pensamento, 'Isto foi dado
no passado, feito no passado, por meu pai e avô. Não seria correto que eu
permitisse que esse antigo costume da família fosse descontinuado' ... na
dissolução do corpo, após a morte, ela renasce no mundo dos devas do Yama. Então tendo esgotado aquela ação, aquele poder, aquele status, aquela
soberania, ela retorna, voltando a este mundo. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Ou, ao invés de pensar
... ela dá uma oferenda com o pensamento, ‘Eu sou próspero. Eles não são
prósperos. Não seria correto, que em sendo eu próspero, não desse uma oferenda
para aqueles que não são prósperos ... na dissolução do corpo, após a morte,
ela renasce no mundo dos devas do Tusita. Então tendo esgotado aquela
ação, aquele poder, aquele status, aquela soberania, ela retorna, voltando a
