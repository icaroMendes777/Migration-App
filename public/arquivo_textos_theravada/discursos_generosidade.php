<p class=Normal><b>Os seis fatores com os quais a generosidade se torna uma fonte incalculável de mérito: </b></p>
<p class=Normal>&nbsp;</p>

<p class=Normal> Em certa ocasião o Abençoado estava em Savathi, no Bosque de Jeta, no Parque de Anathapindika. Agora naquela ocasião a discípula leiga Velukantaki Nandamata estava preparando uma oferenda com seis fatores para a Sangha dos bhikkhus liderada por Sariputta e Moggallana. Com o olho divino que é purificado e sobrepuja o humano, o Abençoado viu a discípula leiga Velukantaki Nandamata preparando a oferenda e ele então se dirigiu aos bhikkhus: </p>

<p class=Normal> "Bhikkhus, a discípula leiga Velukantaki Nandamata está preparando uma oferenda com seis fatores para a Sangha dos bhikkhus liderada por Sariputta e Moggallana. E como uma oferenda possui seis fatores? Aqui, o doador possui três fatores e os recipientes possuem três fatores. </p>

<p class=Normal> "Quais são os três fatores do doador? (1) O doador  sente alegria antes de dar; (2) ele tem uma mente tranquila ao dar; e (3) a sua mente se delicia depois de dar. Esses são os três fatores do doador. </p>

<p class=Normal> "Quais são os três fatores dos recipientes? Aqui, (4) os recipientes estão desprovidos de cobiça ou praticam para a remoção da cobiça; (5) eles estão desprovidos de raiva ou praticam para a remoção da raiva; (6) eles estão desprovidos de delusão ou praticam para a remoção da delusão. Esses são os três fatores dos recipientes. </p>

<p class=Normal> "Assim o doador possui três fatores e os recipientes possuem três fatores. Desse modo a oferenda possui seis fatores. Não é fácil calcular o mérito de uma oferenda dessas assim: 'Tanto assim é essa fonte de méritos, fonte daquilo que é benéfico, alimento da felicidade, celestial, que resulta na felicidade, conduz ao paraíso, conduz ao que é desejável, prazeroso e almejado, para o bem-estar e a felicidade'; ao invés disso é considerado simplesmente como uma grande massa de mérito incalculável, imensurável. Bhikkhus, tal como não é fácil medir a água no grande oceano assim: 'Há tantos litros de água,' ou 'Há tantas centenas de litros de água,' ou 'Há tantos milhares de litros de água,' mas ao invés disso é considerado simplesmente como uma grande massa de água incalculável, imensurável; assim também, não é fácil calcular o mérito de uma oferenda dessas ... ao invés disso é considerado simplesmente como uma grande massa de mérito incalculável, imensurável." </p>

<p class=Normal> Antes de dar há alegria; <br>
enquanto dá a mente está tranquila; <br>
depois de dar a mente se delicia: <br>
esse é o êxito na generosidade. </p>

<p class=Normal> Desprovidos da cobiça e da raiva, <br>
desprovidos da delusão, sem impurezas, <br>
com auto-controle, vivendo a vida santa, <br>
o campo de méritos está completo. </p>

<p class=Normal> Tendo se purificado  <br>
e dado com as próprias mãos, <br>
a generosidade produz muitos frutos<br>
para si mesmo e em relação aos outros. </p>

<p class=Normal> Tendo sido generoso<br>
com a mente livre da avareza, <br>
a pessoa sábia, rica em fé, <br>
renasce num destino feliz, num mundo sem aflições. </p>



