<p class=Normal> “Bhikkhus, há essas cinco oferendas de uma pessoa verdadeira. Quais cinco? Uma pessoa verdadeira dá uma oferenda com base na fé; ela dá uma oferenda respeitosamente; ela dá uma oferenda no momento apropriado; ela dá uma oferenda com o coração empático; ela dá uma oferenda sem afetar de modo adverso a si mesma ou os outros. </p>

