<p class=Normal><b>Aquele que pratica a generosidade não deve temer o rumo de seu próximo renascimento:</b></p>
<p class=Normal>&nbsp;</p>
<p class=Normal> “Por que tantas pessoas aqui estão amedrontadas<br>
quando o caminho foi ensinado com tantas bases?<br>
Eu lhe pergunto, Oh Gotama, amplo em sabedoria:<br>
sobre o que nos devemos apoiar<br>
para não temermos o próximo mundo?”</p>

<p class=Normal> “Tendo corretamente direcionado a linguagem e a mente,<br>
sem praticar ações ruins com o corpo, <br>
habitando o lar com farta comida e bebida,<br>
dotado de fé, gentil, generoso, amigável:<br>
quando alguém se apoia sobre essas quatro coisas,<br>
não se deve temer o próximo mundo.”</p>


