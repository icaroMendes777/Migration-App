<p class=Normal>Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: "Bhikkhus, há dois tipos de generosidade. Quais dois? A generosidade com bens materiais e a generosidade com o Dhamma. Esses são os dois tipos de generosidade. Desses dois tipos, a generosidade com o Dhamma é superior."</p>

