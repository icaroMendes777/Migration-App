<p class=Normal><b>Todas as famílias que hoje vivem em abundância, vivem devido ao fato de terem sido generosas em vidas prévias: </b></p>
<p class=Normal>&nbsp;</p>
<p class=Normal>Em certa ocasião o Abençoado, enquanto perambulava em Kosala juntamente com uma grande comunidade de bhikkhus, chegou em Nalanda ficando no Manguezal de Pavarika. </p>
<p class=Normal>Agora naquela ocasião Nalanda estava no meio de uma grande fome, em uma época de escassez, as lavouras brancas com crestamento e transformadas em palha. E naquela época Nigantha Nataputta estava em Nalanda juntamente com uma grande companhia de niganthas. Então o chefe tribal Asibandhakaputta, um discípulo dos niganthas, foi até Nigantha Nataputta e, ao chegar, depois de cumprimentá-lo, sentou a um lado e o Nigantha Nataputta disse: “Venha, agora, chefe tribal, refute as palavras do contemplativo Gotama, e essa maravilhosa notícia se espalhará ao seu respeito: ‘As palavras do contemplativo Gotama – tão forte tão poderoso – foram refutadas pelo chefe tribal Asibandhakaputta!’”</p>
<p class=Normal> “Mas como, venerável senhor, irei refutar as palavras do contemplativo Gotama – tão forte, tão poderoso?” </p>
<p class=Normal> “Venha agora, chefe tribal, vá até o contemplativo Gotama e diga isto: ‘Venerável senhor, o Abençoado em muitos modos não louva a bondade, proteção e simpatia por famílias?’ Se o contemplativo Gotama, assim questionado, responder, ‘Sim, chefe, o Tathagata em muitos modos louva a bondade, proteção e simpatia por famílias,’ então você deve dizer, ‘Então porque, venerável senhor, o Abençoado, juntamente com uma grande comunidade de bhikkhus, está em Nalanda durante uma época de fome, uma época de escassez, quando as lavouras estão brancas com crestamento e transformadas em palha? O Abençoado está praticando para o arruinamento das famílias. O Abençoado está praticando para o fim das famílias. O Abençoado está praticando para a queda das famílias.’ Quando essa questão com duas pontas for colocada para o contemplativo Gotama, ele não terá como engoli-la ou cuspi-la.” </p>
<p class=Normal>Respondendo, “Como quiser, venerável senhor,” o chefe tribal Asibandhakaputta levantou-se de seu assento, e depois de homenagear o Nigantha Nataputta, mantendo-o à sua direita foi até o Abençoado. Ao chegar, depois de cumprimentá-lo, ele sentou a um lado, e disse  para o Abençoado: “Venerável senhor, o Abençoado em muitos modos não louva a bondade, proteção e simpatia por famílias?” </p>
<p class=Normal> “Sim, chefe, o Tathagata em muitos modos louva a bondade, proteção e simpatia por famílias.” </p>
<p class=Normal> “Então porque, venerável senhor, o Abençoado, juntamente com uma grande comunidade de monges, está em Nalanda durante uma época de fome, uma época de escassez, quando as lavouras estão brancas com crestamento e transformadas em palha? O Abençoado está praticando para o arruinamento das famílias. O Abençoado está praticando para o fim das famílias. O Abençoado está praticando para a queda de famílias.” </p>
<p class=Normal> “Chefe tribal, recordando-me de 91 éons, eu não conheço nenhuma família que tenha sido trazida à ruína através da doação de comida. Pelo contrário: quaisquer famílias que hoje são ricas – com muita opulência, muitas posses, uma grande quantia de dinheiro, um grande acúmulo de riqueza, e muitas comodidades – todas se tornaram assim a partir da generosidade, da verdade, da contenção. </p> 
<p class=Normal> “Chefe tribal, há oito causas, oito motivos para a queda das famílias. Famílias vão para seu fim por causa de reis, ou famílias vão para o seu fim por causa de ladrões, ou famílias vão para seu fim por causa de fogo, ou famílias vão para seu fim por causa de enchentes, ou seus tesouros guardados desaparecem, ou seus empreendimentos são mal administrados, ou na família nasce um vagabundo que esbanja, espalha e espatifa sua fortuna, e a impermanência é a oitava. Estas são as oito causas, os oito motivos para a queda das famílias. Agora, quando estas oito causas, estes oito motivos são encontrados, se alguém disser de mim, ‘O Abençoado está praticando para o arruinamento das famílias. O Abençoado está praticando para o fim das famílias. O Abençoado está praticando para a queda de famílias’ – sem abandonar essa afirmação, sem ter abandonado essa intenção, sem ter descartado essa opinião – então é como se tendo sido arrastado, ele assim seria lançado no inferno.”  </p>
<p class=Normal>Quando isso foi dito, o chefe tribal Asibandhakaputta, disse para o Abençoado: “Magnífico, Venerável senhor! Magnífico, Venerável senhor! O Abençoado esclareceu o Dhamma de várias formas, como se tivesse colocado em pé o que estava de cabeça para baixo, revelasse o que estava escondido, mostrasse o caminho para alguém que estivesse perdido ou segurasse uma lâmpada no escuro para aqueles que possuem visão pudessem ver as formas. Nós buscamos refúgio no Abençoado, no Dhamma e na Sangha dos bhikkhus. Que o Abençoado nos aceite como discípulo leigo que nele buscou refúgio para o resto da vida.”</p>
