<p class=Normal><b>Não seja como uma nuvem que não chove: </b></p>
<p class=Normal>&nbsp;</p>


<p class=Normal>Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: "Bhikkhus, esses três tipos de pessoas podem ser encontradas no mundo. Quais três? Igual a uma nuvem que não chove, igual a uma nuvem que chove num lugar, e igual a uma nuvem que chove em todos lugares.</p>

<p class=Normal>"Como, bhikkhus, é a pessoa igual a uma nuvem que não chove? Aqui, uma certa pessoa não dá nada para ninguém; ela não dá comida, bebida, roupas, veículos, grinaldas, perfumes, ungüentos, camas, moradia, e lamparinas para contemplativos e brâmanes, para os pobres, destituídos e carentes. Esse tipo de pessoa é igual a uma nuvem que não chove.</p>

<p class=Normal>"Como, bhikkhus, é a pessoa igual a uma nuvem que chove num lugar? Aqui, uma certa pessoa dá para alguns mas não dá para outros; comida, bebida, roupas, veículos, grinaldas, perfumes, ungüentos, camas, moradia, e lamparinas ela dá apenas para alguns contemplativos e brâmanes, apenas para alguns dos pobres, destituídos e carentes. Esse tipo de pessoa é igual a uma nuvem que chove num lugar.</p>

<p class=Normal>"Como, bhikkhus, é a pessoa igual a uma nuvem que chove em todos lugares? Aqui, uma certa pessoa dá para todos; comida, bebida, roupas, veículos, grinaldas, perfumes, ungüentos, camas, moradia, e lamparinas ela dá para todos contemplativos e brâmanes, para todos os pobres, destituídos e carentes. Esse tipo de pessoa é igual a uma nuvem que chove em todos lugares.</p>

<p class=Normal>"Esses, bhikkhus, são os três tipos de pessoas podem ser encontradas no mundo."</p>

<p class=Normal style='margin-left:.75in'>Nem para contemplativos, nem para os brâmanes, <br>
nem para os pobres e destituídos<br>
ele distribui o seu estoque<br>
de alimentos e bebidas e bens; <br>
essa pessoa vil é chamada<br>
"igual a uma nuvem sem chuva." </p>

<p class=Normal style='margin-left:.75in'>Para alguns ela não dá, <br>
para alguns ela oferece esmolas; <br>
as pessoas sábias lhe dizem<br>
"igual a uma nuvem que chove num lugar."</p>

<p class=Normal style='margin-left:.75in'>Uma pessoa renomada por sua generosidade, <br>
compassiva em relação a todos os seres, <br>
distribui esmolas com alegria. <br>
"Dê! Dê!" Ela diz.</p>

<p class=Normal style='margin-left:.75in'>Como uma grande nuvem em uma tempestade<br>
que troveja e chove<br>
preenchendo as planícies e os vales, <br>
saturando a terra com água, <br>
assim é essa pessoa.</p>

<p class=Normal style='margin-left:.75in'>Tendo obtido riqueza de forma justa<br>
através do próprio esforço, <br>
ela satisfaz plenamente com alimento e bebida<br>
todos aqueles que precisam. </p>
