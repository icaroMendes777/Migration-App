<p class=Normal><b>Introdução</b></p> 
<p class=Normal>Quando estamos falando dos ensinamentos do Buda, não podemos deixar de lado três aspectos bem importantes: kamma, renascimento e planos de existência. Kamma é a doutrina de que nossas ações geram frutos, resultados. O Buda definiu kamma como intenção (<a href="../sutta/ANVI.63.php">AN VI.63</a>); em outras palavras, tendo intencionado, agimos através da mente, da fala e do corpo. O acúmulo de kamma durante o curso de uma vida inteira é o que nos leva a renascer vez após vez. Estamos presos em um ciclo existencial, de nascimento, envelhecimento e morte. As pessoas estão em contato com isso o tempo todo, mas não percebem a relevância desses três mensageiros. E dentro desse ciclo de renascimentos, temos os diferentes mundos ou planos no qual o fluxo de consciência pode renascer. Aqui mesmo em nossa terra vemos dois destes – o plano humano e o plano dos animais. Fora estes, o Buda havia declarado que existem outros mais do que nossa vã concepção de mundo possa imaginar. </p>
<p class=Normal>É dentro deste contexto que devemos enxergar a importância da generosidade. O objetivo final de nossa jornada como Budistas é eventualmente sair desse ciclo de morte e renascimento, repleto de sofrimento e insatisfação. O Buda traçou três coisas para seus seguidores leigos cultivarem, para que possam avançar nesta jornada – generosidade, virtude e a prática da meditação. Uma pessoa avara, mesquinha, por exemplo, não consegue progredir na meditação devido ao fato de sua mente estar suja com a mancha do egoísmo. </p>
<p class=Normal>Lembro-me muito bem da primeira vez que estava lendo estes suttas e a impressão que eles causaram em mim. Foi numa madrugada de Novembro de 2012, e era um <i>study-guide</i> (guia de estudos) feito pelo Ven. Thanissaro Bhikkhu. Muitos dos suttas que estavam naquele <i>study-guide</i> estão aqui também, com a inclusão de alguns outros que não aparecem na coleção feita por ele. Espero que esta coletânea possa exercer o mesmo efeito na mente dos leitores que exerceu na minha, isto é, a de nos inspirarmos a <i>sermos generosos</i>.  </p>
<p class=Normal>Logo quando iniciei meus estudos dos suttas eu estava mais interessado no lado que pode ser mais atraente para a maioria das pessoas à primeira vista – a recompensa e promessa de um bom renascimento, seja neste mundo ou num mundo celestial. Afinal, quem não gostaria disso, e se para isso basta fazer ações generosas, porque não tentar? O que eu teria a perder com isso? Não muito, não é como se eu fosse doar coisas que iriam me fazer falta. Eu já havia trabalhado com ações sociais antes quando frequentava o centro espírita de minha cidade natal. Lá eles fazem a Campanha do Quilo no primeiro Domingo de todo mês. Coletam alimentos para doarem para pessoas de áreas carentes que vão até lá pegarem uma cesta básica que montamos a partir das doações que recebemos das pessoas. E naquele tempo eu participava disso sem a intenção ou conhecimento de que tal tipo de ação poderia me levar a um rumo mais feliz ainda nesta vida ou na próxima – e minha mente se alegrava (como ainda se alegra) ao fazer este tipo de serviço. Então porque não se sentir mais motivado ainda tendo fé de que há bons resultados para este tipo de ação? </p>
<p class=Normal>Bem, com o passar do tempo meu conhecimento acerca do Dhamma foi amadurecendo e fui refletindo mais a respeito – a vida dos seres humanos é curta, quem tem sorte vai além dos cem anos, mas geralmente em condições debilitadas. E a própria vida dos seres celestiais, por mais longa que seja, e por mais alegre e bem aventurada também, é igualmente impermanente, chega a um fim assim como a dos seres humanos. Então eu refletia, “Não há segurança nem mesmo no mundo dos devas e não é possível prever o curso de renascimento dos seres, se irão nascer em uma boa destinação ou em uma má”. Os suttas dizem que só há uma forma de se garantir bons renascimentos apenas, até o momento em que eventualmente iremos deixar o Samsara – e isto é, tornar-se um Sotapanna ou Alguém que Entrou na Correnteza (que conduz até Nibbana). Desde então toda e qualquer boa ação que faço é com a intenção (kamma) de que eu possa desfrutar deste grande mérito de que minha mente entre na correnteza nesta vida ainda. </p>
<p class=Normal>É importante que mantenhamos esse senso de urgência em nossos corações, sobre a incerteza da vida, a certeza da morte e a incerteza sobre onde iremos renascer futuramente e estarmos nos perguntando – que estamos fazendo para adiantar nosso progresso? Tal senso é o que nos motiva primeiramente a realizarmos ações hábeis. A prática da generosidade dentro deste contexto assume fundamental importância para nós. A bondade que praticamos não é perdida, e o Buda nos promete que cedo ou tarde iremos sempre receber os frutos dela. Para aqueles que se esforçam por realizar o Dhamma aqui mesmo nesta vida, ela é um fator crucial para nosso progresso rumo ao fim do sofrimento. Portanto, espero que todos possam tirar bons proveitos destas leituras e que possamos fazer o bem aos outros, não só com uma mente que esteja almejando as recompensas e frutos destas boas ações, mas sim com uma mente que se inclina a cada vez mais se desprender até alcançar a Outra Margem – Nibbana. </p>
