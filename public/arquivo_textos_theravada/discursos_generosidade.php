<p class=Normal><b>Doações e presentes de coisas materiais são bons, mas o presente do Dhamma excede todas as outras: </b></p>
<p class=Normal>&nbsp;</p>

<p class=Normal> [Uma devata:]</p>

<p class=Normal style='margin-left:.75in'>Um doador de que, é um doador de força?<br> 
Um doador de que, é um doador de beleza? <br>
Um doador de que, é um doador de alívio? <br>
Um doador de que, é um doador de visão? <br>
E quem é um doador de tudo? </p>
 

<p class=Normal> [O Buda:]</p>

<p class=Normal style='margin-left:.75in'>Um doador de comida, é um doador de força. <br>
Um doador de roupas, é um doador de beleza. <br>
Um doador de veículos, é um doador de alívio. <br>
Um doador de lâmpadas, é um doador de visão. <br>
Aquele que doa uma moradia é o que doa tudo. <br>
 Mas aquele que ensina o Dhamma <br>
                       é um doador do Imortal.</p>


