<p class=Normal><b>Escala de ações meritórias:</b></p>
<p class=Normal>&nbsp;</p>
 
<p class=Normal>Em certa ocasião o Abençoado estava em Savatti, no Bosque de Jeta, no Parque de Anathapindika. Então o chefe de família Anathapindika foi até o Abençoado e depois de cumprimentá-lo, sentou-se a um lado, e o Abençoado então disse: “Chefe de família, oferendas ainda são dadas por sua família?” </p>
<p class=Normal> “Oferendas ainda são dadas por minha família, venerável senhor, mas são inferiores: arroz quebrado cozido com farelo, acompanhado de picles em salmoura.” </p>
<p class=Normal><a name=R1></a> “Chefe de família, não importa se a oferenda é inferior ou superior, se é dada de forma descuidada, não é dada com próprias mãos, dada sem demonstrar respeito, dado aquilo que seria jogado fora, dada com a idéia de que nenhum fruto provém dessa ação: seja lá aonde os resultados desta oferenda produza os seus frutos, a mente da pessoa não irá se inclinar para o desfrute de comida esplendida, não irá se inclinar para o desfrute de roupas esplendidas, não irá se inclinar para o desfrute de veículos esplendidos, não irá se inclinar para o desfrute dos esplendidos cinco elementos do prazer de sensual. E seus filhos e filhas, escravos, servos e trabalhadores não lhe darão ouvidos, não o escutarão, não farão com que suas mentes se inclinem para o bem de aprender. E porque é assim? Porque esse é o resultado da desatenção.</p>

<p class=Normal> “Chefe de família, não importa se a oferenda é inferior ou superior, se é dada de forma cuidadosa, dada com próprias mãos, dada demonstrando respeito, dado aquilo que não seria jogado fora, dada com a idéia de que algum fruto provém dessa ação: seja lá aonde os resultados desta oferenda produzam os seus frutos, a mente da pessoa irá se inclinar para o desfrute de comida esplendida, irá se inclinar para o desfrute de roupas esplendidas, irá se inclinar para o desfrute de veículos esplendidos, irá se inclinar para o desfrute dos esplendidos cinco elementos do prazer de sensual. E seus filhos e filhas, escravos, servos e trabalhadores lhe darão ouvidos, o escutarão, farão com que suas mentes se inclinem para o bem de aprender. E porque é assim? Porque esse é o resultado da atenção.</p>
 <p class=Normal> “Certa vez, chefe de família, havia um brâmane chamado Velama. E esta foi a natureza da oferenda, da grande oferenda, que ele deu: Ele deu 84,000 bandejas de ouro cheias de prata, 84,000 bandejas de prata cheias de ouro, 84,000 bandejas de bronze cheias de pedras preciosas. Ele deu 84,000 elefantes com ornamentos de ouro, estandartes de ouro, cobertos com redes de fio de ouro. Ele deu 84,000 carruagens repletas de peles de leão, peles de tigre, peles de leopardos, cobertores com cor de açafrão, com ornamentos de ouro, estandartes de ouro, cobertos com redes de fios de ouro. Ele deu 84,000 vacas leiteiras com amarras de fina juta e baldes de bronze. Ele deu 84,000 donzelas adornadas com brincos de joias. Ele deu 84,000 sofás repletos de cobertores de longo velo, cobertores bordados, tapetes de pele de veados kadalis, cada um com um dossel em cima e almofadas vermelhas de ambos lados. Ele deu 84,000 comprimentos de pano – do linho mais fino, do algodão mais fino, da seda mais fina. Isso, claro, para não dizer nada da comida e bebida, loções e camas: Elas fluíam, por assim dizer, como rios.</p>
<p class=Normal> “Agora, chefe de família, se o pensamento lhe ocorrer ‘Talvez fosse outra pessoa que naquele tempo era o brâmane Velama, que deu aquela oferenda, aquela grande oferenda,’ esta não é forma como deve ser visto. Eu era o brâmane Velama naquela ocasião. Eu dei aquela oferenda, aquela grande oferenda. Mas naquela ocasião não havia ninguém digno de oferendas; ninguém purificou aquela oferenda.</p> 
<p class=Normal> “Se alguém alimentasse uma pessoa consumada em visão [i.e, um sotapanna], isso seria mais frutífero do que a oferenda, a grande oferenda, dada pelo brâmane Velama.</p>
<p class=Normal> “Se alguém alimentasse um sakadagami, isso seria mais frutífero do que a oferenda, a grande oferenda, dada pelo brâmane Velama, e se [em adição a isso] alguém alimentasse cem pessoas consumadas em visão, maior ainda seria o fruto se alguém alimentasse um único sakadagami.” </p>
<p class=Normal> “Se alguém alimentasse um anagami, isso seria mais frutífero do que a oferenda, a grande oferenda, dada pelo brâmane Velama, e se [em adição a isso] alguém alimentasse cem sakadagamis, maior ainda seria o fruto se alguém alimentasse um único anagami.”</p> 
<p class=Normal> “Se alguém alimentasse um arahant, isso seria mais frutífero do que, ... se alguém alimentasse cem anagamis ...” </p>
<p class=Normal> “Se alguém alimentasse um Paccekabuddha, isso seria mais frutífero do que, ... se alguém alimentasse cem arahants ...”</p> 
<p class=Normal> “Se alguém alimentasse um Tathagata, um arahant perfeitamente iluminado ... isso seria mais frutífero do que, .. se alguém alimentasse cem Paccekabuddhas ...” </p>
<p class=Normal> “Se alguém alimentasse uma comunidade de bhikkhus liderada pelo Buda, isso seria mais frutífero do que, ... se alguém alimentasse um Tathagata, um arahant perfeitamente iluminado.”</p>
<p class=Normal> “Se alguém construísse um prédio e o dedicasse à Sangha das quatro direções, isso seria mais frutífero do que, ... se alguém alimentasse uma comunidade de bhikkhus liderada pelo Buda.” </p>
<p class=Normal> “Se com uma mente clara com serena confiança alguém buscasse refúgio no Buda, Dhamma e Sangha, isso seria mais frutífero do que, ... se alguém construísse um prédio e o dedicasse à Sangha das quatro direções.” </p>
<p class=Normal> “Se com uma mente clara com serena confiança alguém se empenhasse em seguir as regras de treinamento – abster-se de matar seres vivos, abster-se de tomar aquilo que não tenha sidodado, abster-se do comportamento sexual impróprio, abster-se da linguagem mentirosa, abster-se de do vinho, álcool e outros embriagantes que causam a negligência – isso seria mais frutífero do que, ... se alguém com uma mente clara com serena confiança buscasse refúgio no Buda, Dhamma e Sangha.”</p>
<p class=Normal> “Se alguém desenvolvesse amor-bondade no coração apenas pelo tempo que leva para puxar o ubre de uma vaca, isso seria mais frutífero do que, ... se alguém com uma mente clara com serena confiança se empenhasse em seguir as regras do treinamento ...”  </p>
<p class=Normal> “Se alguém desenvolvesse a percepção da impermanência apenas pelo tempo que leva para estalar os dedos, isso seria mais frutífero do que a oferenda, a grande oferenda, que o brâmane Velama deu, e se [em adição a isso] alguém alimentasse cem pessoas consumadas em visão, cem sakadagamis, cem anagamis, cem arahants, cem Paccekabuddhas, um Tathagatha – um arahant perfeitamente iluminado – e alimentasse uma comunidade de bhikkhus liderada pelo Buda, e tivesse um prédio construído e dedicado à Sangha das quatro direções, e com uma mente clara com serena confiança buscasse refúgio no Buda, Dhamma e Sangha, e com uma mente clara com serena confiança se empenhasse em  seguir as regras de treinamento, e desenvolvesse amor-bondade no coração apenas pelo tempo que leva para puxar o ubre de uma vaca, maior ainda seria o fruto se alguém desenvolvesse a percepção da impermanência pelo tempo que leva para estalar os dedos.” </p>



