<p class=Normal><b>Quais os resultados para uma pessoa que faz a caridade conscientemente:</b></p>
<p class=Normal>&nbsp;</p>

<p class=Normal> "Bhikkhus, há cinco benefícios da generosidade. Quais cinco? (1) A pessoa é estimada pelas pessoas em geral. (2) A pessoa tem pessoas verdadeiras como companheiros. (3) Ela desfruta de boa reputação. (4) A pessoa não se esquiva das responsabilidades de um chefe de família. (5) Na dissolução do corpo após a morte, ela renasce num destino feliz, no paraíso."</p>

