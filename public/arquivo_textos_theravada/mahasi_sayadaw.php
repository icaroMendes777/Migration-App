<p class=Tit1 align=center style='text-align:center'>Venerável Mahasi Sayadaw</Tit1>

<p class=Normal> O Venerável Mahasi Sayadaw, U Sobhana Mahathera, foi um dos mais destacados mestres de meditação da era moderna e um líder no ressurgimento contemporâneo da meditação Vipassana. Ele nasceu em um vilarejo próximo a Shwebo em 1904, na antiga Birmânia, Myanmar na atualidade, tendo sido ordenado como noviço aos doze anos de idade e como bhikkhu aos vinte anos. Ele se destacou rapidamente pela sua erudição do Cânone Budista em Pali e já no quinto ano após a sua ordenação estava ensinando as escrituras em um monastério em Moulmein.<o:p></o:p></span></p>

<p class=Normal> No oitavo ano após a sua ordenação ele deixou Moulmein em busca de um método claro e efetivo para a prática de meditação. Em Thaton ele conheceu o renomado mestre de meditação Venerável U Narada, também conhecido como Mingun Jetawun Sayadaw, submetendo-se à orientação do Sayadaw  e dedicando-se à prática intensiva de meditação Vipassana.<o:p></o:p></span></p>

<p class=Normal>Em 1941 ele retornou ao seu vilarejo natal e introduziu a prática sistemática de Vipassana na região. Muitas pessoas, monges bem como leigas, adotaram a prática e obtiveram grandes benefícios por meio das cuidadosas instruções do Sayadaw.<o:p></o:p></span></p>

<p class=Normal>Em 1949 o então Primeiro Ministro da Birmânia, U Nu, e Sir U Thwin, membros executivos da Buddha Sasana Nuggaha Association convidaram o Ven. Mahasi Sayadaw para vir a Rangoon para ensinar a prática de meditação. Ele concordou com o pedido e se estabeleceu no Mahasi Thathana Yeiktha Meditation Center, onde conduziu cursos intensivos de meditação Vipassana até a sua morte em 1982.<o:p></o:p></span></p>

<p class=Normal>Sob sua orientação e dos seus discípulos, milhares de pessoas obtiveram treinamento e muitos outros se beneficiaram com a sua abordagem bem definida da prática de meditação. Mais de uma centena de centros afiliados ao Mahasi Thathana Yeiktha Meditation Center foram estabelecidos na Birmânia e o seu método se espalhou para muitos outros países, no Oriente e no Ocidente. De acordo com um censo feito em 1972 o número de iogues treinados em todos os centros de meditação, na Birmânia e no exterior, ultrapassava a marca de 700.000.<o:p></o:p></span></p>

<p class=Normal>Como reconhecimento pelas suas realizações o Ven. Mahasi Sayadaw foi agraciado com a mais elevada distinção acadêmica da Birmânia, o título de Agga Mahapandita, que lhe foi conferido em 1952. Durante o Sexto Concílio Budista, ocorrido em Rangoon de 1954 a 1956, ele desempenhou o papel de Inquisidor <i>(pucchaka)</i>, papel que foi desempenhado pelo Venerável Mahakassapa no Primeiro Concílio Budista. O Ven. Mahasi Sayadaw também foi um dos membros do comitê executivo que teve a responsabilidade, como autoridade final, pela redação de todos os textos editados pelo Concílio.<o:p></o:p></span></p>

<p class=Normal>O Venerável Mahasi Sayadaw é o autor de numerosos textos sobre meditação e escrituras Budistas no seu idioma nativo, o Birmanês. Os seus discursos abrangendo os suttas Budistas foram traduzidos para o inglês e publicados pela Buddha Sasana Nuggaha Association (16 Hermitage Road, Kokine, Rangoon, Myanmar).<o:p></o:p></span></p>





