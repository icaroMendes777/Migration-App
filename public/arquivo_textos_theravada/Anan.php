<p class=Tit1 align=center style='text-align:center'>Ajaan Anan Akiñcano</Tit1>


<p class=Normal>O Venerável Ajaan Anan Akiñcano nasceu na cidade de Saraburi, na região central da Tailândia, no dia 31 de Março de 1954, com o nome de Anan  Chan-in. Desde pequeno ele regularmente acompanhava os pais nas visitas ao templo local para os cânticos e as homenagens aos monges, e ele se recorda que experienciava  sentimentos de grande tranqüilidade e alegria sempre que via as imagens do Buda. Na escola, ele era conhecido pelas boas maneiras e dedicação aos estudos e depois de formado foi contratado como contador pela Siam Cement Company. Embora fosse um funcionário dedicado, ele notou um crescente interesse pela prática Budista e durante as horas fora do trabalho passou a viver num monastério próximo.  <o:p></o:p></span></p>


<p class=Normal>No ano seguinte, trabalhando e rigorosamente observando os oito preceitos do praticante leigo, ele foi capaz de incrementar os seus esforços na meditação e a sua confiança nos ensinamentos do Buda. Todas as manhãs ele oferecia alimentos aos monges, e depois seguia para o trabalho para mais tarde retornar ao monastério para praticar meditação. Um dia ele teve uma experiência de um profundo insight sobre a natureza de todos os fenômenos, seguido de três dias e três noites sentindo uma felicidade que não se parecia a nada que ele havia experimentado antes. Essa experiência eliminou qualquer dúvida que ele ainda pudesse ter sobre dedicar a sua vida aos ensinamentos do Buda, e pouco tempo depois ele decidiu ingressar na vida monástica. <o:p></o:p></span></p>



<p class=Normal>No dia 3 de Julho de 1975 ele foi ordenado como monge tendo como mestre e preceptor o Venerável Ajaan Chah. Ele recebeu o nome Akiñcano, que significa "aquele que está livre de tudo," e passou os quatro anos seguintes praticando com diligência a meditação e desenvolvendo o cuidado e a atenção em todas as rotinas da vida monástica. Durante esse período ele atuou como o assistente pessoal de Ajaan Chah, o que lhe deu a oportunidade de desenvolver uma conexão próxima com o seu mestre. Embora a sua meditação tivesse altos e baixos durante os primeiros anos como monge, no seu quarto ano alcançou um ponto do qual nunca mais declinou. <o:p></o:p></span></p>


<p class=Normal>Depois do período de treinamento intensivo com Ajaan Chah, ele teve a oportunidade de buscar lugares mais isolados para dar continuidade aos seus esforços na meditação. Vivendo em florestas remotas e cemitérios, ele enfrentou vários tipos de dificuldades tanto externas como íntimas. Mesmo assim ele permaneceu comprometido com esse estilo de vida, desenvolvendo tolerância, mesmo lutando contra sérios ataques de malária que quase lhe tomaram a vida em muitas ocasiões. Em 1984 foi-lhe oferecido uma área desabitada  para construir um monastério das florestas. Acompanhado por dois outros monges e um noviço, o grupo se estabeleceu numa densa floresta na província de Rayong,  num lugar agora conhecido como Wat Marp Jan, "Monastério da Montanha Iluminada pela Lua."</p>

<p class=Normal>Mais de 25 anos mais tarde, a reputação de Ajaan Anan  como mestre de meditação cresceu, junto com o número de monges que vêm para praticar com ele e buscar os seus conselhos. O monastério e os arredores se desenvolveram de modo considerável desde que ele chegou, refletindo tanto a presença de Ajaan  Anan bem como a sua contribuição para a região. Na atualidade, ele cuida das suas muitas tarefas como abade e mestre, dividindo o seu tempo entre os monges e os visitantes leigos, e cuidando de um crescente número de monastérios afiliados.</p>

