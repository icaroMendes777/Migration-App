<p class=Normal>As pessoas gastam quase metade do tempo em que estão acordadas pensando em alguma outra coisa do que aquilo que estão fazendo, e essa dispersão mental tipicamente traz a infelicidade. Isso é o que foi verificado num estudo que usou um aplicativo para o iPhone para colher 250.000 dados sobre os pensamentos, emoções e ações dos participantes durante a sua rotina diária. </p>

<p class=Normal>A pesquisa conduzida pelos psicólogos Matthew A. Killingsworth e Daniel T. Gilbert da Harvard University, foi descrita na revista Science do mês de Novembro de 2010. </p>

<p class=Normal>"A mente humana é uma mente dispersa e uma mente dispersa é uma mente infeliz," relatam Killingsworth e Gilbert. "A habilidade de pensar sobre o que não está acontecendo é um grande avanço cognitivo mas que tem um custo emocional." </p>

<p class=Normal>Ao contrário de outros animais, os humanos gastam muito tempo pensando sobre coisas que não estão acontecendo à sua volta: contemplando eventos que aconteceram no passado, que podem acontecer no futuro, ou que nunca acontecerão. Na verdade, parece que a mente dispersa é o modo de operação automático do cérebro humano. </p>

<p class=Normal>Para avaliar esse comportamento, Killingsworth desenvolveu um aplicativo para o iPhone que contatava 2.250 voluntários em intervalos aleatórios perguntado-lhes quão felizes se sentiam, o que estavam fazendo naquele momento e se estavam pensando sobre a atividade que estavam realizando ou sobre qualquer outra coisa - e, neste caso, se era um pensamento agradável, neutro ou desagradável. </p>

<p class=Normal>Os participantes podiam escolher de uma lista de 22 atividades gerais como caminhando, comendo, comprando ou assistindo televisão. Em média os respondedores relataram que as suas mentes estavam divagando em 46.9% do tempo e que nada menos do que 30% durante qualquer tipo de atividade exceto na relação sexual. </p>

<p class=Normal>"A dispersão mental parece estar presente em todas atividades," diz Killingsworth, que estuda para o doutorado em Psicologia na Harvard. "Este estudo mostra que as nossas vidas estão impregnadas, num grau significativo, pela não-presença." </p>

<p class=Normal>Killingsworth e Gilbert, que é professor de psicologia na Harvard, descobriram que as pessoas se sentiam mais felizes numa relação sexual, se exercitando ou conversando. Elas se sentiam menos felizes descansando, trabalhando ou usando um computador. </p>

<p class=Normal>"A dispersão mental é um indicador excelente para predizer a felicidade das pessoas," diz Killingsworth. "Na verdade, a freqüência que as nossas mentes abandonam o presente e para onde tendem a ir é um melhor indicador para predizer a nossa felicidade do que as atividades com as quais nos ocupamos." </p>

<p class=Normal>Os pesquisadores estimaram que apenas 4,6% da felicidade de uma pessoa num determinado momento podia ser atribuída a uma atividade específica que ele ou ela estavam desempenhando, enquanto que o estado de dispersão mental respondia por cerca de 10,8% da sua felicidade. </p>

<p class=Normal>Análises de "intervalo causa-efeito" (time-lag) conduzidas pelos pesquisadores sugerem que a dispersão mental dos participantes era em geral a causa, não a conseqüência, da sua infelicidade. </p>

<p class=Normal>"Muitas tradições religiosas e filosóficas ensinam que a felicidade pode ser encontrada ao viver no momento presente e os praticantes são treinados a evitar a dispersão mental e 'permanecer no aqui e agora,'" relatam Killingsworth e Gilbert na revista Science. "Essas tradições sugerem que uma mente dispersa é uma mente infeliz." </p>

<p class=Normal>Essa nova pesquisa, dizem os autores, sugere que essas tradições estão corretas. </p>

<p class=Normal>Os 2.250 participantes no estudo de Killingsworth e Gilbert tinham entre 18 e 88 anos, com uma ampla diversidade sócio-econômica e de ocupações, sendo que 74% dos participantes eram americanos. </p>

<p class=Normal>Mais de 5.000 pessoas estão agora usando o aplicativo para o iPhone que está disponível para download no site www.trackyourhappiness.org. </p>


