<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-language:PT-BR'>= o surgimento de todos esses <i
style='mso-bidi-font-style:normal'>dhammas<o:p></o:p></i></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Note que o
estado mental hábil transcendente pode começar na ignorância ou numa raiz
hábil, mas o estado mental transcendente resultante começa numa raiz hábil ou, se
não for assim, então num impulso volitivo. Além disso, a frase final “e assim é
o surgimento de toda essa massa de sofrimento e estresse” se torna “e assim o
surgimento de todos esses dhammas.”<o:p></o:p></span></p>

<p class=Normal><b><a name=T4></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>Um problema
com a palavra “nirodha”<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A palavra <i
style='mso-bidi-font-style:normal'>nirodha</i> tem sido traduzida como
“cessação” há tanto tempo que já se tornou a prática padrão e qualquer desvio
disso resulta em questionamentos. Mesmo neste livro optei por essa tradução
padrão visando a conveniência e para evitar confudí-la com outros termos em
Pali (além da inexistência de uma outra palavra melhor). Na verdade, de
qualquer modo, essa tradução da palavra &quot;<i style='mso-bidi-font-style:
normal'>nirodha</i>&quot; como &quot;cessar&quot; pode em muitos casos levar a
uma interpretação inadequada do texto.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>De modo geral, a palavra
“cessar” significa eliminar algo que já havia surgido, ou de interromper algo
que já havia começado. No entanto, <i style='mso-bidi-font-style:normal'>nirodha,
</i>no ensinamento da Origem Dependente, (como também em <i style='mso-bidi-font-style:
normal'>dukkhanirodha</i>, a terceira Nobre Verdade), significa o não
surgimento ou não existência de algo porque a causa do seu surgimento foi
eliminada. Por exemplo, a frase “quando <i style='mso-bidi-font-style:normal'>avijja
</i><span style='mso-bidi-font-style:italic'>é </span><i style='mso-bidi-font-style:
normal'>nirodha</i>, <i style='mso-bidi-font-style:normal'>sankhara </i><span
style='mso-bidi-font-style:italic'>também é</span><i style='mso-bidi-font-style:
normal'> nirodha</i>,” que em geral é interpretada como “com a cessação da
ignorância, as formações volitivas cessam,” na verdade significa “quando não há
ignorância, ou a ignorância não surge, ou quando não há mais qualquer problema
com a ignorância, não existem formações volitivas, as formações volitivas não surgem, ou não existe
mais nenhum problema com as formações volitivas.” Não significa que a ignorância que já
surgiu tenha que ser eliminada antes que as formações volitivas que já surgiram também
sejam eliminados.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Onde <i style='mso-bidi-font-style:
normal'>nirodha</i> deve ser interpretado como cessação é quando é usado em
referência ao processo natural das coisas, ou a natureza das coisas
condicionadas. Nesse sentido é um sinônimo para as palavras <i
style='mso-bidi-font-style:normal'>bhanga</i>, dissolução, <i style='mso-bidi-font-style:
normal'>anicca</i>, impermanente, <i style='mso-bidi-font-style:normal'>khaya</i>,
cessação ou <i style='mso-bidi-font-style:normal'>vaya</i>, deterioração. Por
exemplo, em Pali:<span style="mso-spacerun: yes">  </span><i style='mso-bidi-font-style:
normal'>imam kho bhikkhave tisso vedana anicca sankhata paticcasamuppanna
khayadhamma vayadhamma viragadhamma nirodhadhamma</i>: “Bhikkhus, esses três
tipos de sensações são por natureza impermanentes, condicionadas, com origem
dependente, impermanentes, sujeitas à deterioração, dissolução e cessação.”
[SN.IV.214] (Todos os fatores que ocorrem no ciclo da Origem Dependente têm a
mesma natureza.) Neste caso, o significado é, “todas as coisas condicionadas (<i
style='mso-bidi-font-style:normal'>sankhara</i>), tendo surgido, devem de modo
inevitável se dissolver e cessar de acordo com os fatores que lhes dão
suporte.” Não é necessário tentar cessá-las, elas cessam por si mesmas. Neste
caso a intenção é descrever uma condição natural que, na prática simplesmente
significa que “aquilo que surge pode ser eliminado.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quanto a <i
style='mso-bidi-font-style:normal'>nirodha</i> na terceira Nobre Verdade (ou o
ciclo da Origem Dependente no seu modo de cessação), embora esta também seja
descrita como um processo natural, a ênfase está nas considerações de ordem
prática. No Visuddhimagga a tradução ocorre de duas formas. Uma forma rastreia
a etimologia até<span style="mso-spacerun: yes">  </span>&quot;ni&quot; (sem) +
&quot;rodha&quot; (prisão, confinamento, obstáculo, parede, impedimento), dessa
forma interpretando o significado como “sem impedimentos,” “livre de
confinamento.” Isso é explicado como “livre de impedimentos, isto é, do
confinamento de samsara.” Outra definição busca a origem em <i
style='mso-bidi-font-style:normal'>anuppada</i>, que significa “não
surgimento,” e diz, “<i style='mso-bidi-font-style:normal'>nirodha </i>neste
caso não significa <i style='mso-bidi-font-style:normal'>bhanga, </i>rompimento
e dissolução.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então,
traduzir <i style='mso-bidi-font-style:normal'>nirodha </i>como “cessação,”
embora não seja completamente errado, não é no entanto totalmente acurado. Por
outro lado, não existe nenhuma outra palavra que se aproxime da essência do
significado tal como a palavra “cessação.” No entanto, devemos compreender o
que se quer dizer com o termo. Nesse contexto, o ciclo da Origem Dependente no
seu modo de cessação poderia ser melhor descrito como, “estando livre da
ignorância,<span style="mso-spacerun: yes">  </span>fica-se livre das
formações volitivas...” ou “quando a ignorância se foi, as formações volitivas se vão...” ou
“quando a ignorância cessa de dar o seu fruto, as formações volitivas cessam de dar os
seus frutos...” ou “quando a ignorância não é mais um problema, as formações volitivas
não são mais um problema.”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Mesmo no modo para adiante,
existem alguns problemas com as definições. O significado de alguns termos em
Pali são demasiado amplos para serem encapsulados em uma única palavra em
Inglês ou Português. Por exemplo, <i style='mso-bidi-font-style:normal'>avijja
paccaya</i> também significa “Quando a ignorância é assim, as formações volitivas são
assim; sendo as formações volitivas assim, a consciência é assim; sendo a
consciência<span style="mso-spacerun: yes">  </span>assim, a
mentalidade-materialidade (nome e forma) são assim;...”<o:p></o:p></span></p>

