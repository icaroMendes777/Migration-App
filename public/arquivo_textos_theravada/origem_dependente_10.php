<p class=Normal><b><a name=T1></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>Uma nota
sobre como interpretar o princípio da Origem Dependente<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Foi
mencionado antes que no comentário do Abhidhamma Pitaka, (<i>Sammohavinodani</i>),
o princípio da Origem Dependente é mostrado ocorrendo de forma completa durante
um momento mental. Este ponto merece ser reiterado porque o estudo moderno dos
ensinamentos (pelo menos nos círculos acadêmicos tradicionais)<span
style="mso-spacerun: yes">  </span>interpreta a sua ocorrência de uma forma
completa com base na duração de uma vida para outra. Como conseqüência, quando
existe qualquer intento de interpretar o ciclo da Origem Dependente como sendo
um processo que ocorre na vida diária, aqueles que se agarram a uma interpretação
mais limitada querem descartá-lo como sem fundamento ou em contradição com as
escrituras. Portanto, visando o conforto mútuo e a paz de espírito, incluí esta
referência para mostrar que essa interpretação não está desprovida de
fundamento nas escrituras.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Na verdade,
vale a pena observar que o que existe de evidência para essa interpretação é
provavelmente apenas uma sombra do passado que se tornou quase esquecido e que
ainda existe apenas porque o Tipitaka se mantém como uma referência
irrefutável.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Os
comentários que descrevem o ciclo da Origem Dependente como um processo de uma
vida para outra, que em geral é aceito como sendo fidedigno, provém do <i>Visuddhimagga</i>,
escrito por Acariya Buddhaghosa por volta do século cinco da era cristã. No
entanto, existe um outro comentário que trata do princípio da Origem Dependente
que é o<span style="mso-spacerun: yes">  </span><i>Sammohavinodani</i>
mencionado acima. Neste caso, a explicação é dividida em duas seções, a
primeira que trata do princípio da Origem Dependente como um processo baseado
no período de duração de<span style="mso-spacerun: yes">  </span>uma vida para
outra, como no Visuddhimagga, e uma segunda que explica o princípio como um
evento que ocorre no espaço de um momento mental.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O <i>Sammohavinodani</i>
também é uma obra de Acariya Buddhaghosa e acredita-se que tenha sido escrita depois
do <i>Visuddhimagga</i>. A diferença entre os dois é que enquanto o <i>Visuddhimagga</i>
foi escrito pelo próprio Acariya Buddhaghosa, o <i>Sammohavinodani</i> é um
comentário do Abhidhamma Pitaka transcrito por ele. Na introdução ao <i>Sammohavinodani</i>,
Buddhaghosa escreve, “Eu compilarei esta obra dos comentários antigos.”
[Vibh.A. 1] Mesmo no <i>Visuddhimagga</i>, na seção que trata do princípio da
Origem Dependente, ele revela que, “Uma explicação da Origem Dependente é
extremamente difícil,” e<span style="mso-spacerun: yes">  </span>“Agora,
gostaria de explicar o <i>paccayakara,</i> (princípio da condicionalidade),
muito embora não tenha onde me apoiar, como um homem ao entrar num rio sem
pedras sobre as quais pisar. No entanto, o princípio da Origem Dependente é
muito rico em ensinamentos, sem mencionar os comentários transmitidos por
antigos mestres numa linha contínua. Contando com essas duas fontes, eu irei
agora expor o princípio da Origem Dependente.”[Vism.522; idêntico ao
Vibh.A.130]<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>A
explicação do princípio da Origem Dependente dada no <i>Visuddhimagga</i>, ao
contrário do <i>Sammohavinodani</i>, contém apenas a explicação do princípio
baseado no período de<span style="mso-spacerun: yes">  </span>duração de uma
vida para outra. Esta explicação é praticamente idêntica àquela encontrada no <i>Sammohavinodani</i>.
Sendo esse o caso, cabe a pergunta, “Porque o <i>Visuddhimagga </i>não traz a
explicação do princípio da Origem Dependente em um momento mental?” Pode ser
que mesmo no tempo de<span style="mso-spacerun: yes">  </span>Buddhaghosa os
círculos escolásticos em geral descreviam o princípio da Origem Dependente
baseados no período de duração de uma vida para outra. Também pode ser que o
autor se sentisse mais à vontade com esta interpretação devido à complexidade
do tema, como ele mesmo observou na introdução, no entanto, existiam os
comentários dos mestres transmitidos até aquela época. A interpretação do
momento mental, por outo lado, não era só muito complexa, mas também havia
desaparecido dos círculos escolásticos. Isto pode ser inferido do próprio <i
style='mso-bidi-font-style:normal'>Sammohavinodani</i>, onde a descrição da
interpretação do momento mental é extremamente breve.<span style="mso-spacerun:
yes">  </span>E uma justificação para isso pode ser simplesmente pelo fato de
haver uma menção sobre essa interpretação no Tipitaka, e portanto demandar uma
explicação. O autor foi capaz de fazer uso dos resquícios dos comentários que
ainda existiam para formular o seu próprio comentário.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Vamos agora
considerar a explicação dada no <i style='mso-bidi-font-style:normal'>Sammohavinodani.
</i>O <i style='mso-bidi-font-style:normal'>Sammohavinodani</i> é um comentário
do <i style='mso-bidi-font-style:normal'>Vibhanga</i>, que é o segundo volume
do Abhidhamma Pitaka. A seção do <i style='mso-bidi-font-style:normal'>Vibhanga</i>
que descreve o princípio da Origem Dependente é denominada <i style='mso-bidi-font-style:
normal'>Paccayakara Vibhanga</i>. Essa seção está dividida em duas partes: a
primeira denominada <i style='mso-bidi-font-style:normal'>Suttantabhajaniya,</i>
(definição de acordo com os Suttas), a segunda <i style='mso-bidi-font-style:
normal'>Abhidhammabhajaniya</i> (definição de acordo com o Abhidhamma). O <i
style='mso-bidi-font-style:normal'>Sammohavinodani</i>, que é o comentário
dessa seção, está também dividido em duas partes. A descrição da diferença
entre essas duas partes é a seguinte:<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“A primeira
parte explica <i style='mso-bidi-font-style:normal'>paccayakara</i> em relação
aos inúmeros momentos de consciência no <i style='mso-bidi-font-style:normal'>Suttantabhajaniya</i>,
mas como o <i style='mso-bidi-font-style:normal'>paccayakara</i> não está
limitado a numerosas mentes e pode até mesmo ocorrer num momento mental, a
explicação agora é como o paccayakara ocorre num momento mental e isso está no <i
style='mso-bidi-font-style:normal'>Abhidhammabhajaniya</i>.&quot; [Vibh.A.199]
E num outro trecho: “No <i style='mso-bidi-font-style:normal'>Suttantabhajaniya
</i><span style='mso-bidi-font-style:italic'>o</span> <i style='mso-bidi-font-style:
normal'>paccayakara</i> está dividido em várias vidas. No <i style='mso-bidi-font-style:
normal'>Abhidhammabhajaniya</i> está explicado em um momento mental.”
[Vibh.A.200] Com relação ao princípio de causa e efeito, como este opera em um
momento mental na vida diária, é dito que, “...nascimento (envelhecimento e
morte), por exemplo, neste caso se refere ao nascimento (envelhecimento e
morte) de coisas imateriais (<i style='mso-bidi-font-style:normal'>arupa</i>),
não à deterioração dos dentes, agrisalhar dos cabelos, enrugar da pele, morte,
à ação de deixar a vida.” [Vibh.A.208]<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Um último
ponto merece menção: No <i style='mso-bidi-font-style:normal'>Vibhanga</i> do
Tipitaka, a seção que descreve a interpretação de uma vida para outra ocupa
apenas cinco páginas de texto. A seção que descreve o princípio da Origem
Dependente num momento mental contém setenta e duas páginas.[<a href="#N26">26</a>]
<a name=R26></a>Mas no <i style='mso-bidi-font-style:normal'>Sammohavinodani</i>,
o comentário de Buddhaghosa é o reverso. Isto é, a parte que trata da
interpretação de uma vida para outra é longa, contendo 92 páginas, enquanto que
a parte que trata da interpretação do momento mental contém apenas 19 páginas.
[<a href="#N27">27</a>] <a name=R27></a>A razão porque o comentário da versão
do momento mental da Origem Dependente é tão curto pode ter sido porque o autor
não tinha muito que dizer a respeito. Ou talvez ele pensasse que isso já tinha
sido suficientemente explicado no Tipitaka, não havendo a necessidade de
comentários adicionais. Qualquer que seja o caso, podemos afirmar que a
interpretação da Origem Dependente na vida diária existia desde o princípio e é
encontrada no Tipitaka, mas nos Comentários apenas resquícios dela são
encontrados.<o:p></o:p></span></p>

<p class=Normal><b><a name=T2></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>Nascimento
e morte no momento presente<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Aqueles que
gostariam de ver uma referência ao ciclo de renascimento no momento presente,
na vida atual, podem preferir consultar o Sutta apresentado abaixo:<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“’Bhikkhu, uma pessoa possui quatro fundamentos.’ Assim foi dito. Com
referência a que foi dito isso? Existe o fundamento da sabedoria, o fundamento
da verdade, o fundamento da renúncia, o fundamento da paz. As torrentes das
concepções não arrastam aquele que se mantém sobre eles (fundamentos) e quando
as torrentes das concepções não o arrastam ele é chamado um sábio em paz..<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Bhikkhu, 'Eu sou’ é uma concepção; 'Eu sou isto' é uma concepção; 'Eu
serei’ é uma concepção. 'Eu não serei' é uma concepção; 'Eu possuirei forma' é
uma concepção; 'Eu não possuirei forma' é uma concepção; 'Eu serei perceptivo'
é uma concepção; 'Eu não serei perceptivo' é uma concepção; 'Eu serei nem
perceptivo,nem não perceptivo' é uma concepção. A concepção é uma enfermidade,
a concepção é um câncer, a concepção é uma flecha. Superando todas as
concepções, se diz que ele é um sábio em paz. E um sábio em paz não nasce, não
envelhece, não morre, não é agitado e está livre de desejos. Pois não existe
nada presente nele pelo qual ele possa renascer. Não nascendo, como poderá
envelhecer? Não envelhecendo, como poderá morrer? Não morrendo, como ficará
agitado? Não ficando agitado, o que irá desejar? <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Portanto foi com referência a isso que foi dito: 'As torrentes das
concepções não arrastam aquele que se mantém sobre eles [fundamentos] e quando
as torrentes das concepções<span style="mso-spacerun: yes">  </span>não o
arrastam ele é chamado um sábio em paz.’[<a href="#N28">28</a>]<o:p></o:p></span></p>

<p class=Normal><b><a name=R28></a><a name=T3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>A Origem Dependente no Abhidhamma<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>No
Abhidhamma são apresentados muitos modelos distintos da Origem Dependente,
organizados de acordo com os vários tipos de estados mentais hábeis, inábeis e
neutros envolvidos na sua geração. Além disso os modelos também são analisados
de acordo com o estado mental envolvido, quer seja do reino sensual, (<i
style='mso-bidi-font-style:normal'>kamavacara</i>), do reino da forma, (<i
style='mso-bidi-font-style:normal'>rupavacara</i>), do reino sem forma, (<i
style='mso-bidi-font-style:normal'>arupavacara</i>), ou do reino transcendente,
(<i style='mso-bidi-font-style:normal'>lokuttara</i>). Isso é devido ao fato de
o Abhidhamma estudar a mente no nível de “momentos de pensamento” e assim
analisar a Origem Dependente de acordo com o estado mental específico envolvido.
Os fatores que ocorrem nesses modelos variam de acordo com o tipo de estado
mental.<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Por
exemplo, em alguns estados mentais hábeis, o modelo pode começar em <i
style='mso-bidi-font-style:normal'>sankhara</i>, formações volitivas, sem a presença da ignorância,
ou pode até mesmo começar numa das raízes da habilidade, (não cobiça, não raiva
e não delusão), ao invés da ignorância. Especialmente digno de nota é o fato de
que o desejo apenas ocorre nos modelos baseados nos estados mentais inábeis. Em
alguns casos, o desejo é substituído por <i style='mso-bidi-font-style:normal'>pasada</i>,
inspiração, ou é totalmente excluído. A ignorância e o desejo são suprimidos
nessas ocasiões – eles não aparecem no seu formato padrão mas de outras formas,
isso se não forem completamente excluídos. Além disso no Abhidhamma Pitaka os
vários fatores são apresentados como componentes de um todo ou como ações
contrárias (tal como “ignorância condiciona formações volitivas, formações volitivas condicionam a
ignorância; formações volitivas condicionam a consciência, a consciência condiciona as formações volitivas;”
etc.) Aqui apresentarei apenas as descrições mais importantes:<o:p></o:p></span></p>

