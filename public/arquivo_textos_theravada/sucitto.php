<p class=Tit1 align=center style='text-align:center'>Ajaan Sucitto</Tit1>


<p class=Normal>O Venerável Ajaan Sucitto nasceu em Londres em 1949, mas mudou-se para a cidadezinha de Dunstable na adolescência. Seu primeiro encontro com o Budismo veio através de um interesse pela literatura japonesa enquanto estudava no colegial, mas na sua cidade ele não conseguiu encontrar nada para dar continuidade a esse assunto. O contínuo interesse na literatura fez com que ele obtivesse o diploma de bacharel em inglês e literatura americana na Universidade de Warwick em 1971. Depois disso, a busca por um direcionamento que desse um significado na vida atraiu-o para uma viagem através do leste em direção à Austrália. Depois de um período na Índia, em 1975 ele foi para a Tailândia, onde ele acabou se deparando com uma aula de meditação Budista em Chiang Mai. Depois de alguns dias de prática, ele decidiu tentar um comprometimento com Vida Santa, ordenando-se como bhikkhu em 1976.</p>

<p class=Normal>Ele passou três anos na Tailândia, a maior parte do tempo em Wat Kiriwong em Nakhon Sawan. Durante uma curta estada em Chiang Mai, no entanto, ele encontrou Ajaan Sumedho antes deste partir para a Inglaterra. E quando Ajaan Sucitto retornou à Inglaterra em 1978 para visitar sua família, ele encontrou Ajaan Sumedho novamente no Vihara Hampstead e decidiu ficar para fazer um treinamento com ele. Durante os anos que ele esteve com Ajaan Sumedho, ele foi responsável pela edição e publicação das palestras dele, bem como de outras publicações da Sangha.</p>

<p class=Normal>Ajaan Sucitto fez parte do grupo original que fundou o Monastério de Chithurst, em Cittavivekha, em 1979. Ele também ajudou a estabelecer um vihara em Northumberland, Aruna Ratanagiri, em 1981. Em 1984 ele fez parte da comunidade que mudou de Chithurst para Hertfordshire para começar o Monastério Budista de Amaravati onde ele tinha a responsabilidade de supervisionar as monjas que estavam em treinamento, bem como outras obrigações. Nessa época, ele também começou a ensinar o Budismo fora da Inglaterra. Em 1992, ele foi chamado para assumir a função de abade do Monastério Chithurst onde ele agora reside.</p> 

<p class=Normal>Suas publicações incluem:<br>
“Introdução à Meditação de Insight” (um guia para principiantes)<br>
“O Amanhecer do Dhamma” (um comentário ilustrado sobre o primeiro discurso do Buddha)<br> 
“Kalyana” (uma pequena coleção de palestras editadas)</p>

<p class=Normal>Ele também faz parte do corpo de professores visitantes do BCBS – Barre Center of Buddhist Studies em Barre/MA, EUA.</p>



