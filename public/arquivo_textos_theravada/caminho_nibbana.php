<p class=Normal>Existe
um velho ditado que diz que o caminho para o inferno está pavimentado com boas
intenções, mas isso não corresponde à verdade. O caminho para o inferno está
pavimentado com intenções que são descuidadas, cobiçosas ou más. Boas intenções
– na proporção da sua verdadeira bondade - conduzem aos paraísos divinos. Então
porque elas têm uma reputação tão ruim? Devido principalmente a três razões.
Primeiro que nem todas as boas intenções são particularmente hábeis. Apesar das
intenções pretenderem ser boas, elas podem ser equivocadas e inadequadas para a
ocasião, dessa forma resultando em dor e arrependimento. Uma segunda razão é
que com freqüência confundimos a qualidade das nossas próprias intenções.
Podemos confundir uma intenção mista como sendo boa por exemplo, e dessa forma
ficarmos desapontados quando os resultados são mesclados. Uma terceira razão é
que podemos facilmente interpretar de maneira equivocada a forma como as
intenções produzem os seus resultados - como quando os resultados dolorosos de
uma má intenção no passado obscurecem os resultados de uma boa intenção no
presente, e apesar disso colocamos a culpa na nossa intenção no presente pela
dor. Todas essas razões, agindo em conjunto, fazem com que nos desiludamos com
o potencial das boas intenções. Como resultado, ou nos tornamos cínicos em
relação a elas ou então simplesmente abandonamos o cuidado e a paciência
necessários para aperfeiçoá-las. <o:p></o:p></span></p>

<p class=Normal>Uma
das descobertas mais sagazes do Buda foi que as nossas intenções são os
principais fatores que moldam as nossas vidas e que elas podem ser dominadas
como uma habilidade qualquer. Se as sujeitarmos às mesmas qualidades de atenção
plena, persistência e sabedoria requeridas para o desenvolvimento de qualquer
outra habilidade, poderemos aperfeiçoá-las até o ponto em que elas não conduzam
a nenhuma lamentação ou resultados danosos em qualquer situação; no final das
contas elas podem nos conduzir à mais verdadeira felicidade. No entanto, para
treinar as nossas intenções desta forma, requer um profundo nível de
autoconsciência. Por que isso? Se você olhar com cuidado para os motivos da
nossa desilusão com as boas intenções, descobrirá que todos têm a ver com a
delusão: delusão sobre como formulamos as nossas intenções, delusão em como
percebemos as nossas intenções e delusão em como nos preocupamos com os seus
resultados. O Buda nos diz que a delusão é uma das três principais raízes dos
hábitos mentais inábeis e prejudiciais, sendo as outras duas, o desejo e a raiva.
Essas raízes inábeis e prejudiciais encontram-se embaralhadas com as raízes
hábeis e benéficas - estados da mente que estão livres do desejo, raiva e
delusão - no solo do coração sem treino. Se não pudermos isolar e desenterrar
as raízes inábeis, não poderemos nunca estar completamente seguros das nossas
intenções. Mesmo quando uma intenção hábil pareça estar ocupando a mente, as
raízes inábeis podem rapidamente enviar ramificações que nos cegarão para
aquilo que na verdade está acontecendo. <o:p></o:p></span></p>

<p class=Normal>Se
fossemos fazer um esboço de como são essas coisas, o quadro seria mais ou menos
o seguinte: o caminho direto para o inferno está pavimentado com as más
intenções, algumas das quais podem parecer boas num relance superficial. Os
caminhos pavimentados com as boas intenções, que conduzem aos paraísos divinos,
alguns deles bastante hábeis, se ramificam para ambos os lados da trilha, mas
com muita freqüência eles se perdem nos arbustos da falta de habilidade e
acabamos nos encontrando de volta no caminho para o inferno. A descoberta do
Buda foi que se alimentarmos as raízes hábeis, elas poderão crescer e
efetivamente bloquear o caminho para o inferno; se cortarmos fora os arbustos
da falta de habilidade e desenterrarmos as suas raízes, poderemos desenvolver
as nossas boas intenções em níveis cada vez mais elevados de habilidade, até
que no final das contas elas nos conduzam a uma felicidade completamente
ilimitada que está além da necessidade de um caminho.<o:p></o:p></span></p>

<p class=Normal>O
passo fundamental nesse processo é assegurar que permaneçamos fora do caminho
que conduz ao inferno. Fazemos isso através da prática da generosidade e
virtude, conscientemente substituindo as intenções inábeis por aquelas mais
hábeis. Em seguida refinamos as nossas intenções ainda mais através da
meditação, desenterrando as raízes do desejo, raiva e delusão para evitar que
elas influenciem as escolhas que moldam as nossas vidas. O desejo e a raiva às
vezes são fáceis de detectar, mas a delusão - por sua própria natureza - é
obscura. Quando estamos deludidos, nós não <i>sabemos </i>que estamos
deludidos. É por isso que através da meditação temos que focar no
fortalecimento e agilização dos nossos poderes de atenção plena e plena
consciência, para que possamos identificar a delusão e desenraizá-la antes que
ela assuma o controle das nossas mentes. <o:p></o:p></span></p>

<p class=Normal>As
instruções de meditação mais elementares do Buda para o refinamento das
intenções começam, não na almofada de meditação mas, com as atividades da vida
diária. Elas estão contidas em um discurso para o seu jovem filho, Rahula, e
atacam o ‘Catch-22’ (a causa é o efeito e o efeito é a causa – ex.: preciso dos
meus óculos para achar os meus óculos) da delusão através de duas abordagens. A
primeira é aquilo que os textos Budistas mais antigos denominam &quot;reflexão
ou atenção com sabedoria” <i>(yoniso manasikara)</i> - a habilidade de
perguntar a si mesmo as questões certas, questões que vão direto às causas do
prazer e da dor, sem embaralhar a mente numa confusão desnecessária. A segunda
abordagem é a amizade com pessoas admiráveis - associar-se e aprender com
pessoas que são virtuosas, generosas e sábias. Esses dois fatores, disse o
Buda, são os elementos mais importantes de ajuda interna e externa para uma
pessoa que está seguindo o caminho. <o:p></o:p></span></p>

<p class=Normal>Em
essência, o Buda disse para Rahula que usasse as suas ações como um espelho
para refletir a qualidade da sua mente. A cada momento antes de agir - e nesse
caso &quot;agir&quot; abrange qualquer ação com o pensamento, linguagem ou
corpo - ele deveria refletir acerca do resultado que ele esperava obter da ação
e perguntar a si mesmo: &quot;Esta ação irá causar dano a mim e aos outros, ou
não?&quot; Se ela fosse danosa, ele não deveria praticá-la. Se ela parecesse
inócua, ele poderia seguir em frente e agir. No entanto, o Buda preveniu Rahula
de que ele não deveria confiar cegamente nas suas expectativas. Enquanto ele
estivesse agindo, ele deveria perguntar a si mesmo se havia alguma conseqüência
má inesperada surgindo. Se houvesse, ele deveria parar. Se não houvesse, ele
poderia seguir com a sua ação até o final. Mesmo assim, no entanto, a tarefa de
reflexão não estava terminada. Ele também deveria observar as conseqüências da
ação a curto e longo prazo. Se uma ação com a linguagem ou com o corpo causasse
algum dano, então ele deveria informar um companheiro da vida santa e ouvir o
conselho que ele poderia lhe dar. Se a ação equivocada fosse puramente uma ação
mental, então ele deveria desenvolver um senso de vergonha e temor em relação a
esse tipo de pensamento. Em ambos os casos, ele deveria tomar a resolução de
nunca mais cometer tal erro. Se, no entanto, as conseqüências da ação original
fossem inócuas, ele deveria se alegrar por estar no caminho correto e continuar
com o seu treinamento. <o:p></o:p></span></p>

<p class=Normal>A
partir disso, podemos ver que a abordagem básica para revelar a delusão é o
conhecido princípio de aprender através dos próprios erros. A maneira como o
Buda formula esse princípio, no entanto, tem implicações importantes, pois ela
exige qualidades de honestidade consigo mesmo e maturidade naquela área em que
em geral é difícil encontrá-las: nossa avaliação das nossas próprias intenções
e dos resultados das nossas ações. <o:p></o:p></span></p>

<p class=Normal>Quando
crianças aprendemos a ser desonestos com respeito a nossas intenções
simplesmente por uma questão de sobrevivência: &quot;Eu não tinha intenção de
fazer isso&quot;, &quot;Eu não pude evitar&quot;, &quot;Eu estava só balançando
o meu braço e ele se meteu no meio&quot;. Depois de algum tempo passamos a
acreditar nas nossas próprias desculpas e não gostamos de admitir quando as
nossas intenções deixam de ser nobres. Assim nos habituamos a não articular as
nossas intenções quando temos que fazer uma escolha, ou nos recusamos a
considerar as conseqüências das nossas intenções, e em muitos casos, de negar
que desde o princípio tínhamos escolha. Assim é como se formam os hábitos de
comportamento e como as intenções inábeis tomam as rédeas. <o:p></o:p></span></p>

<p class=Normal>Uma
dinâmica semelhante envolve as nossas reações às conseqüências das nossas
ações. Aprendemos a negar com tenra idade - &quot;Não foi minha culpa&quot;,
&quot;Já estava quebrado quando eu o peguei&quot; - e a interiorizar o
processo, como forma de preservar a nossa auto- imagem, até o ponto em que<span
style="mso-spacerun: yes">  </span>ignorar o impacto causado pelos nossos erros
se converte<span style="mso-spacerun: yes">  </span>num hábito. <o:p></o:p></span></p>

<p class=Normal>O
Buda indicou que o fim do sofrimento requer que abandonemos o desejo e a
ignorância, mas se não pudermos ser honestos conosco a respeito das nossas
intenções, como poderemos perceber o desejo a tempo de abandoná-lo? Se não
pudermos encarar o princípio de causa e efeito nas nossas ações, como poderemos
superar a ignorância? A ignorância é causada menos por falta de informação do
que por falta de autoconsciência e honestidade para consigo mesmo. Para
compreender as nobres verdades requer que sejamos honestos conosco precisamente
naquelas áreas em que a honestidade consigo mesmo é mais difícil. <o:p></o:p></span></p>

<p class=Normal>Isso
também requer maturidade. Ao examinar as nossas intenções, precisamos aprender
como dizer não para as motivações inábeis de tal forma que sejamos
suficientemente firmes para mantê-las sob controle, mas não tão firmes a ponto
de fazer com que elas se escondam no subconsciente reprimido. Podemos aprender
a ver a mente como um comitê: o fato de que impulsos impróprios são propostos
por membros do comitê não significa que <i>nós </i>sejamos impróprios. Não
temos que assumir a responsabilidade por tudo aquilo que é apresentado no
comitê. A nossa responsabilidade está, ao invés disso, no nosso poder de
aceitar ou vetar a moção. <o:p></o:p></span></p>

<p class=Normal>Ao
mesmo tempo, devemos ser adultos o suficiente para admitir que os nossos
impulsos habituais ou espontâneos não são sempre confiáveis - o primeiro
pensamento não é sempre o melhor pensamento - e aquilo que estamos inclinados a
fazer agora pode não produzir resultados agradáveis mais tarde. O Buda disse
que em todos os momentos existem quatro ações alternativas: uma que queremos
fazer e que dará bons resultados; uma que não queremos fazer e que dará maus
resultados; uma que queremos fazer mas que dará maus resultados; e uma que não
queremos fazer mas que dará bons resultados. Nas duas primeiras não há muito o
que pensar. Não é necessário ser muito inteligente para realizar a primeira e
evitar a segunda. A medida da nossa verdadeira inteligência está em como
lidamos com as duas últimas opções. <o:p></o:p></span></p>

<p class=Normal>Examinar
os resultados das nossas ações também requer maturidade: a compreensão madura
de que a auto-estima não pode estar baseada em estar sempre certo e de que não
existe nada de humilhante ou degradante em admitir um erro. Todos nós nos
encontramos num estado de delusão - até mesmo o Buda quando partiu em busca da
Iluminação - portanto é natural que ocorram erros. A nossa dignidade humana se
encontra na habilidade em reconhecer esses erros, tomar a decisão de não
repeti-los e ater-se a essa decisão. Por outo lado isso requer que não nos
debilitemos por sentimentos de culpa ou remorso pelos nossos erros. O Buda
disse que sentimentos de remorso não podem desfazer um erro cometido e eles
podem privar a mente da força que ela necessita para evitar repetir os mesmos
erros. É por isso que ele recomenda uma emoção distinta do remorso - vergonha -
embora o uso que ele tenha dado a essa palavra implique algo totalmente
diferente da noção de indignidade que com freqüência associamos a esse termo.
Lembre-se de que tanto o Buda como Rahula eram membros da nobre casta de
guerreiros, uma classe com uma forte noção da própria honra e dignidade. E
observe que o Buda diz a Rahula para ver os seus erros, não a si mesmo, como
vergonhosos. Isso implica que está abaixo da dignidade de Rahula agir de uma
forma que seja menos do que honrável. O fato de ele ser capaz de ver as suas
ações como vergonhosas é um indício da sua honra - e também é um sinal de que
ele será capaz de não repeti-las. Essa noção de honra é o que está por debaixo
da noção de vergonha madura, saudável e produtiva. <o:p></o:p></span></p>

<p class=Normal>A
primeira vista, podemos pensar que a constante reflexão da forma descrita pode
adicionar mais complicações às nossas vidas que já parecem complicadas o
suficiente, mas na verdade as instruções do Buda são um esforço em despir as
questões nas nossas mentes até os seus elementos mais úteis. Ele explicitamente
nos adverte quanto a tomar questões em excesso, particularmente aquelas que não
conduzem a nada e que nos confundem: &quot;Quem sou eu? Sou essencialmente uma
pessoa boa?&quot; Ao invés disso, ele nos diz que foquemos nas nossas intenções
para que possamos ver como elas moldam as nossas vidas, e com isso dominemos o
processo de causa e efeito de maneira que ele possa moldar a nossa vida de um
jeito melhor, cada vez mais. Essa é a maneira como todo grande artista ou
artesão desenvolve a sua maestria e habilidade.<o:p></o:p></span></p>

<p class=Normal>A
ênfase nas intenções que estão por trás das nossas ações e as conseqüências que
delas resultam também se transferem da vida diária para a meditação, provendo a
nossa meditação com o foco adequado. Ao examinar as nossas ações em termos de
causa e efeito, hábeis e inábeis, já estamos começando a olhar para a
experiência em concordância com os dois conjuntos de variáveis que compõem as
quatro nobres verdades: a origem do sofrimento (uma causa inábil), o caminho
para a cessação do sofrimento (uma causa hábil), o sofrimento (um efeito
inábil) e a cessação do sofrimento (um efeito hábil). A forma como o Buda
recomenda que Rahula julgue os resultados das suas ações - tanto enquanto elas
estão sendo praticadas como após a sua prática - ecoa o insight que formou o núcleo
da sua iluminação: que as intenções geram resultados, tanto no presente
imediato como ao longo do tempo. <o:p></o:p></span></p>

<p class=Normal>Quando
olharmos para o momento presente sob essa perspectiva, veremos que a nossa
experiência do presente não &quot;acontece simplesmente&quot;. Ao invés disso,
ela é o produto do nosso envolvimento – em relação às nossas intenções no
presente, os resultados dessas intenções e os resultados de intenções passadas
– sendo que, as intenções presentes são o fator mais importante. Quanto mais
foquemos nesse envolvimento, mais poderemos retirá-lo da meia luz do
subconsciente e trazê-lo para a plena luz da consciência. Ali podemos treinar
as nossas intenções, através da tentativa e erro consciente, para que sejamos
ainda mais habilidosos, tornando-nos capazes de diminuir a nossa experiência de
sofrimento e dor no presente. Assim é como as intenções habilidosas pavimentam
o caminho para a saúde mental e bem estar no mundo em que vivemos. <o:p></o:p></span></p>

<p class=Normal>Conforme
trabalhamos no desenvolvimento das nossas intenções para níveis de habilidade
cada vez mais elevados, descobrimos que as intenções mais perfeitas são aquelas
que centralizam a mente com solidez em uma clara consciência do presente. À
medida que as usamos, para ficar mais e mais familiarizados com o presente,
podemos começar a ver que <i>todas </i>as intenções no presente, não importando
quão hábeis, são no seu íntimo fatigantes. A única forma de nos livrarmos desse
peso é<span style="mso-spacerun: yes">  </span>permitir que as intenções que
proporcionam a trama da nossa experiência presente se desembaracem. Isso
oferece uma abertura para a dimensão da liberdade ilimitada que está além
delas. Assim é como as intenções hábeis pavimentam o caminho até a fronteira de
nibbana. A partir dali, o caminho - &quot;como o dos pássaros através do
espaço&quot; - não pode ser traçado. <o:p></o:p></span></p>


