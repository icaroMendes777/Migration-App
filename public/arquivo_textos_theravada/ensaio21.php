<p class=Normal>O primeiro passo ao iniciar o caminho Budista é a busca do refúgio na Jóia
Tríplice e a primeira das três jóias na qual buscamos refúgio é o Buda, o
Iluminado. Pelo fato de que a busca de refúgio no Buda marca o início de um
novo capítulo na nossa vida, vale a pena pausar e refletir repetidas vezes
sobre o significado desse passo tão importante. Com demasiada freqüência somos
inclinados a tomar os nossos primeiros passos de forma leviana. E somente se
revisarmos de tempos em tempos esses passos, dotados de uma consciência mais
profunda das suas implicações, é que poderemos estar seguros de que os próximos
passos que tomarmos estarão nos levando mais próximo do destino almejado. </p>

<p class=Normal>A busca de refúgio no Buda não é um ato único, que ocorre uma só vez<span
style="mso-spacerun: yes">  </span>e então se conclui totalmente. Ela é, ou
deveria ser, um processo de evolução contínuo que amadurece em sincronia com a
nossa prática e compreensão do Dhamma. Tomar o refúgio não significa que desde
o princípio já possuímos uma compreensão clara dos perigos que fazem com que um
refúgio seja necessário ou do objetivo pelo qual aspiramos. A compreensão
desses assuntos cresce gradualmente com o tempo. Mas na medida em que tenhamos
buscado refúgio com sinceridade, deveríamos nos esforçar seriamente para aguçar
e aprofundar o nosso entendimento dos objetos nos quais estamos nos baseando
para a nossa libertação.</p>

<p class=Normal>Ao buscar refúgio no Buda é essencial, desde o princípio, clarificar o nosso
conceito sobre o que é um Buda e como é que ele funciona como um refúgio. Se
essa clareza não existir, a nossa noção acerca do refúgio pode facilmente ficar
deturpada por idéias incorretas. Podemos atribuir ao Buda um status que ele
nunca reivindicou para si mesmo, tal como quando o tomamos como uma
reencarnação de um deus, como uma emanação do Absoluto, ou como um salvador dos
seres. Por outro lado, podemos diminuir o status glorificado ao qual o Buda tem
todo direito, como quando o consideramos simplesmente como um sábio
benevolente, como um filósofo Asiático extraordinariamente astuto, ou como um
gênio da técnica de meditação. </p>

<p class=Normal>Uma idéia correta sobre a natureza do Buda seria vê-lo baseado no título que
ele consignou a si mesmo: Alguém Perfeitamente Iluminado por Si Mesmo <i
style='mso-bidi-font-style:normal'>(samma sambuddha).</i> Ele é iluminado por
si mesmo porque ele despertou para as verdades essenciais da existência,
inteiramente por si mesmo, sem um mestre ou guia. Ele é perfeitamente iluminado
porque compreendeu essas verdades completamente, em todas as suas ramificações
e implicações. E como um Buda, ele não somente penetrou e compreendeu essas
verdades, mas também ele as ensinou ao mundo de forma que outros pudessem
despertar do longo sono da ignorância e alcançar os frutos da libertação. </p>

<p class=Normal>A toma de refúgio no Buda é um ato ancorado em um indivíduo histórico em
particular: o contemplativo Gotama, o descendente do clã dos Sakyas, que viveu
e ensinou no vale do rio Ganges no quinto século antes de Cristo. Quando
tomamos refúgio no Buda, depositamos nossa confiança nesse indivíduo histórico
e no corpo de ensinamentos que se originou dele. É importante enfatizar esse
ponto, em vista da noção em moda de que tomar refúgio no Buda significa
tomarmos refúgio na &quot;mente Búdica dentro de nós&quot; ou no
&quot;princípio universal da iluminação&quot;. Tais idéias, se forem aceitas
sem serem questionadas, podem conduzir à crença de que qualquer coisa concebida
pelas asas da nossa imaginação pode ser qualificada como o verdadeiro Dhamma.
Ao contrário, a tradição Budista insiste em que, ao tomarmos refúgio no Buda,
nos coloquemos sob a orientação de alguém que é claramente diferente de nós,
alguém que escalou alturas que nós apenas começamos a vislumbrar. </p>

<p class=Normal>Mas, quando depositamos nossa confiança no contemplativo Gotama como nosso
refúgio, nós não o tomamos simplesmente como um indivíduo em particular, um
sábio inteligente e sensível. Nós o tomamos como um Buda. É a sua qualidade de
Buda – sua posse do escopo completo de qualidades excelentes que acompanham a
perfeita iluminação -<span style="mso-spacerun: yes">  </span>que faz do contemplativo
Gotama um refúgio. Em qualquer época cósmica, um Buda é aquele ser que primeiro
rompe através da massa escura de ignorância que cerca o mundo e redescobre o
caminho perdido para Nibbana, a cessação do sofrimento. Ele é o pioneiro, quem
abre o caminho, quem descobre o caminho e proclama o caminho de forma que
outros, seguindo as suas pegadas, possam extinguir a ignorância, conquistar a
verdadeira sabedoria e romper os grilhões que os aprisionam ao repetido ciclo
de nascimento e morte. </p>

<p class=Normal>Para que o refúgio no Buda seja genuíno, ele tem que ser
acompanhado de um compromisso com o Buda como um mestre incomparável,
insuperável e superior. Em termos estritos, o Buda histórico não é único já que
houveram Seres Perfeitamente Iluminados que surgiram em épocas passadas e
haverão outros que irão surgir em épocas futuras também. Mas em qualquer
sistema cósmico é impossível que um segundo Buda surja enquanto os ensinamentos
de um outro Buda ainda existirem e dessa forma, sob a perspectiva da história
humana se justifica considerar o Buda como um mestre único, não igualado por
qualquer outro mestre espiritual conhecido pela humanidade. É essa disposição
em reconhecer o Buda como o &quot; treinador insuperável de pessoas preparadas
para serem treinadas, o mestre de seres divinos e humanos&quot;, que indica um
ato autêntico de toma de refúgio no Buda. </p>

<p class=Normal>O Buda serve como um refúgio por ensinar o Dhamma. O refúgio real e último,
embutido no Dhamma, é Nibbana, &quot;o elemento imortal livre do apego, o
estado isento de tristezas que está vazio de impurezas&quot; (<a name=PVW>Itiv</a>.
51). O Dhamma como refúgio compreende o objetivo último, o caminho que conduz a
esse objetivo e o corpo de ensinamentos que explicam a prática do caminho. O
Buda como refúgio não tem capacidade para nos conceder a libertação por um ato
de vontade. Ele proclama o caminho a ser percorrido e os princípios que devem
ser compreendidos. A tarefa de realizar o caminho é deixada para nós, os seus
discípulos. </p>

<p class=Normal>A resposta adequada ao Buda como refúgio é a confiança e a certeza. A
confiança é necessária porque a doutrina ensinada pelo Buda vai em sentido
contrário ao nosso entendimento inato do que somos e a nossa orientação natural
em relação ao mundo. Portanto, a aceitação desses ensinamentos tende a
despertar uma resistência interna, até mesmo a provocar uma rebelião contra as
mudanças, que eles demandam que façamos, na forma como vivemos as nossas vidas.
Porém, quando depositamos nossa confiança no Buda, aceitamos a sua orientação.
Ao tomar refúgio nele, mostramos que estamos preparados para reconhecer que as
nossas tendências inatas à auto afirmação e ao apego são na verdade as causas
do nosso sofrimento. E estamos preparados para aceitar o seu conselho de que
para nos libertarmos do sofrimento, essas tendências precisam ser controladas e
eliminadas. </p>

<p class=Normal>A confiança no Buda como nosso refúgio é inicialmente despertada quando
contemplamos as suas virtudes sublimes e o seu ensinamento excelente. Ela
cresce através da adoção do treinamento. Inicialmente, a nossa confiança no
Buda pode ser hesitante, pontuada por dúvidas e perplexidades. Mas, à medida
que nos aplicamos à prática do seu caminho, nos damos conta de que as nossas
impurezas (mentais) gradualmente diminuem, que as qualidades benéficas se
incrementam e com isso surge uma crescente sensação de liberdade, paz e
alegria. Essa experiência confirma a nossa confiança inicial, predispondo-nos a
avançar alguns passos mais. Quando por fim vemos a verdade do Dhamma por nós
mesmos, o refúgio no Buda se torna inviolável. A confiança então se torna
convicção, a convicção de que o Abençoado é &quot;o orador, o que proclama, que
traz o bem, que oferece o Imortal, o senhor do Dhamma, o Tathagata.&quot; </p>


