<p class=Normal><b>Segunda Parte</b><o:p></o:p></span></p>

<p class=Normal>
Dando
continuidade ao ensaio anterior, tenho a intenção de discutir três áreas em que
existem diferenças significativas entre os ensinamentos do Buda, ao qual podemos
nos referir simplesmente como o &quot;Dhamma dos Ariyas&quot; e as filosofias
da não-dualidade. Essas áreas correspondem às três divisões do caminho Budista
- virtude, concentração e sabedoria.<o:pp></o:p></span></pp>

<p class=Normal>
Com respeito à <i
style='mso-bidi-font-style:normal'>virtude,</i> a diferença entre os dois
ensinamentos não é evidente de imediato, já que ambos geralmente afirmam a
importância da conduta virtuosa no início do treinamento. A diferença
fundamental entre ambos emerge não no início, mas somente mais tarde, pela
forma como eles avaliam o papel da virtude nos estágios mais avançados do
caminho. Para os sistemas não-duais, todas as dualidades são finalmente
transcendidas através da realização da não-dualidade da realidade, o Absoluto
ou fundamento essencial. Como o Absoluto abrange e transcende toda diversidade
para aquele que o realizou, as distinções entre o bem e o mal, virtude e não
virtude, perdem a sua validade última. Tais distinções, se diz, são apenas
válidas num nível convencional, não no nível da realização última; elas são
obrigatórias para o iniciante, mas não para o discípulo habilidoso. Dessa forma
encontramos que nos seus formatos históricos (particularmente no Tantra Hindu e
Budista), as filosofias da não-dualidade defendem que a conduta de um sábio
iluminado não pode ser limitada por regras morais. O sábio transcendeu todas as
distinções convencionais tradicionais do bem e do mal. Ele age espontaneamente
à partir da sua intuição da Verdade Última e portanto não está mais limitado
pelas regras de virtude válidas para aqueles que ainda estão se esforçando em
alcançar a luz. O seu comportamento é um fluxo elusivo e incompreensível
daquilo que se denomina &quot;louca sabedoria&quot;.<o:p></o:p></span></p>

<p class=Normal>
No Dhamma dos
Ariyas, a distinção entre os dois tipos de conduta, moral e imoral, é rígida e
cristalina e essa distinção permanece por todo o percurso até a consumação do
caminho: &quot;A conduta corporal é de dois tipos, eu digo: para ser cultivada
e para não ser cultivada. E a conduta corporal ou é uma, ou é a outra&quot; (<a
href="../sutta/MN114.php">MN 114</a>).<span style="mso-spacerun: yes"> 
</span>A conduta do perfeito sábio Budista, o Arahant, necessariamente
incorpora o padrão mais elevado de integridade moral <i style='mso-bidi-font-style:
normal'>ambos </i>na essência <i style='mso-bidi-font-style:normal'>e</i> na
forma, e para ele a conformidade com a forma é natural e espontânea. O Buda diz
que o libertado vive contido pelas regras do Vinaya, vendo o perigo nas menores
falhas. Ele não é capaz de intencionalmente violar os preceitos morais, nem
nunca iria tomar alguma atitude motivada pelo desejo, raiva, delusão ou temor.<o:p></o:p></span></p>

<p class=Normal>
Na esfera da <i
style='mso-bidi-font-style:normal'>prática meditativa</i> ou concentração, nós
novamente encontramos uma diferença significativa entre a<span
style="mso-spacerun: yes">  </span>perspectiva dos sistemas não-duais e o
Dhamma dos Ariyas. Visto que, para os sistemas não-duais, as distinções são em
última instância irreais, a prática meditativa não é explicitamente orientada
para a remoção das impurezas e o cultivo de estados mentais virtuosos. Nesses
sistemas, se diz com freqüência que as impurezas são apenas aparências
despojadas de uma realidade intrínseca e até mesmo manifestações do Absoluto.
Portanto, engajar em um programa de prática para superá-las é um exercício
fútil, tal como fugir de uma aparição demoníaca: buscar a eliminação das
impurezas significa reforçar a ilusão da dualidade. Os temas de meditação que
povoam as correntes de pensamento não-dualistas afirmam: &quot;nenhuma impureza
e nenhuma pureza&quot;; &quot;as impurezas são em essência o mesmo que a
sabedoria transcendente&quot;; &quot;é através da paixão que a paixão é
eliminada&quot;.<o:p></o:p></span></p>

<p class=Normal>
No Dhamma dos
Ariyas, a prática de meditação se desdobra do início ao fim como um processo de
purificação mental. O processo se inicia com o reconhecimento dos perigos dos
estados prejudiciais: eles são verdadeiros poluidores do nosso ser que precisam
ser contidos e eliminados. A consumação é atingida com a completa destruição
das impurezas através do cultivo dos seus antídotos benéficos. Todo o conteúdo
da prática demanda um reconhecimento das diferenças entre as qualidades
sombrias e luminosas da mente e se apóia no esforço e na diligência: &quot;Ele
não tolera um pensamento prejudicial que tenha surgido; ele o abandona, o remove,
o elimina, o aniquila&quot; (<a href="../sutta/MN2.php">MN 2</a>). Os
obstáculos<span style="mso-spacerun: yes">  </span>&quot;causam a cegueira,
causam a falta de visão, causam a ausência de conhecimento, prejudicam a
sabedoria, tendem para a aflição, afastam de Nibbana&quot; (<a
href="../sutta/SNXLVI.40.php">SN XLVI.40</a>). A prática de meditação purifica
a mente das suas corrupções, preparando o caminho para a destruição das
impurezas <i style='mso-bidi-font-style:normal'>(asavakkhaya).</i> <o:p></o:p></span></p>

<p class=Normal>
Finalmente, no
domínio da <i style='mso-bidi-font-style:normal'>sabedoria</i> o Dhamma dos
Ariyas e os sistemas não-duais se movem em direções contrárias. Nos sistemas
não-duais a tarefa da sabedoria é de romper as aparências diversificadas (ou a
aparência de diversidade) de modo a descobrir a realidade unificadora que está
por trás delas. Os fenômenos concretos, nas suas distinções e na sua pluralidade,
são apenas aparências, enquanto que a verdadeira realidade é Única: ou um
Absoluto com substância (o Atman, Brahma, Deus, etc.), ou um zero metafísico
(Sunyata, a Natureza Vazia da Mente, etc.). Para tais sistemas, a libertação
surge alcançando a unidade fundamental em que os opostos se fundem e as
diferenças evaporam como o orvalho.<o:p></o:p></span></p>

<p class=Normal>
No Dhamma dos
Ariyas a sabedoria tem como objetivo ver e conhecer as coisas como elas
realmente são <i style='mso-bidi-font-style:normal'>(yathabhutananadassana).</i>
Portanto, para conhecer as coisas como elas realmente são, a sabedoria precisa
respeitar os fenômenos na sua exata particularidade. A sabedoria deixa a
diversidade e pluralidade intocadas. Ela, ao contrário, busca descobrir as
características dos fenômenos, conquistar o insight das suas qualidades e
estruturas. Ela se move, não na direção de uma identificação abrangente com o
Todo, mas em direção ao desligamento e desapego, a libertação do Todo. O
cultivo da sabedoria de forma nenhuma &quot;subverte&quot; os fenômenos
concretos reduzindo-os a aparências, nem os trata como se fossem janelas que se
abrem para algum fundamento essencial. Ao invés disso, ela investiga e discerne
de forma a entender as coisas como elas são: &quot;E o que alguém entende ser
como na verdade é? Ele entende que: assim é a forma, assim é a sua origem e
cessação. Assim é a sensação... percepção... formações... consciência, assim é
a sua origem e cessação.&quot; &quot;Quando ele vê,<span style="mso-spacerun:
yes">  </span>'Todas as formações são impermanentes, todas são insatisfatórias,
todas são não-eu', ele se afasta do sofrimento: esse é o caminho da
purificação.&quot; <o:p></o:p></span></p>

<p class=Normal>
Os sistemas
espirituais são coloridos tanto pelos seus símiles favoritos como pela doutrina
formulada. Para os sistemas não-duais, dois símiles se destacam como
predominantes. Um é o espaço, que simultaneamente engloba tudo e permeia tudo e
no entanto não tem nada de concreto em si mesmo; o outro é o oceano, que mantém
a mesma natureza sob a multiplicidade das ondas. Os símiles usados no Dhamma
dos Ariyas são bastante diversos, mas um tema que une muitos deles é a acuidade
da visão - visão que discerne o panorama de formas visíveis com clareza e
precisão, cada uma na sua própria individualidade. &quot;Tal como se houvesse
uma lagoa num vale em uma montanha<span style="mso-spacerun: yes">  </span>-
clara, límpida e cristalina  em que um homem com boa visão, em pé na margem,
pudesse ver conchas, cascalho e seixos e também cardumes de peixes nadando e
descansando, isso lhe ocorreria, Esta lagoa tem a água clara, límpida e cristalina.
Ali estão aquelas conchas, cascalho e seixos e também aqueles cardumes de
peixes nadando e descansando.
 Da mesma forma -
com a sua mente assim concentrada, purificada, clara, pura, imaculada, livre de
defeitos, flexível, maleável, estável e atingindo a imperturbabilidade  o
bhikkhu a dirige e a inclina para o conhecimento do fim das impurezas mentais.
Ele compreende, da forma como na verdade é que Isto é sofrimento...Esta é a
origem do sofrimento...Esta é a cessação do sofrimento...Este é o caminho que
conduz à cessação do sofrimento...Essas são impurezas mentais...Esta é a origem
das impurezas...Esta é a cessação das impurezas...Este é o caminho que conduz à
cessação das impurezas. Ao conhecer e ver,<span style="mso-spacerun: yes"> 
