<p class=Normal><b>Primeira Parte</b><o:p></o:p></span></p>

<p class=Normal>
Um dos temas mais
desafiadores que o Budismo Theravada tem enfrentado nos últimos anos é o encontro
entre a clássica meditação vipassana Theravada e as tradições contemplativas
&quot;não-dualistas&quot; que são melhor representadas pelo Advaita Vedanta e o
Budismo Mahayana. As reações a esse encontro abrangem os extremos, indo desde a
confrontação veemente até a tentativa de síntese e hibridização. Enquanto que o
presente ensaio não tem a pretensão de esclarecer toda a intrincada e sutil
problemática envolvida nesse diálogo ocasionalmente volátil, eu espero poder
contribuir com alguns lampejos de claridade sob a perspectiva do cânone
Theravada.<o:p></o:p></span></p>

<p class=Normal>
Minha primeira
observação preliminar é insistir em que um sistema de prática meditativa não
constitui uma disciplina independente. Qualquer sistema de prática espiritual
autêntico sempre é encontrado inserido dentro de uma matriz conceitual que
define os problemas que a prática tem como propósito solucionar e o objetivo ao
qual ela está direcionada. Por conseguinte a fusão de técnicas que se baseiam
em sistemas conceituais incompatíveis está repleta de riscos.<span
style="mso-spacerun: yes">  </span>Embora tais fusões possam satisfazer uma
predileção pela experimentação ou ecletismo, parece provável que o seu efeito a
longo prazo será o de criar uma certa &quot;dissonância cognitiva&quot; que irá
ecoar nos níveis mais profundos da psique e provocar uma confusão ainda maior. <o:p></o:p></span></p>


<p class=Normal>
Minha segunda
observação é de simplesmente apontar que as tradições espirituais não-dualistas
estão longe de serem consistentes umas com as outras, mas compreendem,
particularmente, uma ampla gama de idéias profundamente diferentes entre si e inevitavelmente
coloridas pelo contorno conceitual mais abrangente das filosofias que as
englobam.<o:p></o:p></span></p>

<p class=Normal>
Para o Vedanta, a
não-dualidade <i style='mso-bidi-font-style:normal'>(advaita)</i> significa a
ausência de uma distinção última entre o Atman, o eu mais profundo, e Brahma, a
realidade divina, o fundamento do mundo. Sob a perspectiva da realização mais
elevada, somente uma realidade última existe - que é simultaneamente Atman e
Brahma - e o objetivo da busca espiritual é de compreender que o verdadeiro eu,
o Atman, é a realidade eterna que é o Ser, Consciência, Êxtase. Visto que todas
as escolas do Budismo rejeitam a idéia do Atman, nenhuma pode aceitar o
não-dualismo do Vedanta. Sob a perspectiva da tradição Theravada, qualquer
busca para a descoberta de um eu, quer seja como um eu permanente individual ou
como um eu absoluto universal, deveria ser descartada como uma delusão, uma
tolice metafísica originada da inabilidade de compreender adequadamente a
natureza da experiência concreta. De acordo com os Suttas em Pali, o ser é
apenas uma unidade complexa com cinco agregados, todos eles gravados com as
três marcas da impermanência, sofrimento e ausência de um eu. Qualquer hipótese
da existência de um eu em relação a esse composto de fenômenos condicionados e
transitórios, é um exemplo de &quot;concepção de uma identidade&quot; <i
style='mso-bidi-font-style:normal'>(sakkayaditthi),</i> o grilhão mais básico
que prende os seres ao ciclo de renascimentos. A realização da libertação, no
Budismo, não passa pela realização de um eu verdadeiro ou &quot;eu&quot;
absoluto, mas através da dissolução de até mesmo a mais sutil noção de um eu em
relação aos cinco agregados, &quot;a abolição de todas as fabricações de
&quot;eu&quot;, fabricações de meu e as tendências subjacentes à
presunção.&quot; <o:p></o:p></span></p>

<p class=Normal>
As escolas do
Mahayana, apesar das suas grandes diferenças, coincidem em sustentar uma tese,
que sob o ponto de vista Theravada, beira o ultrajante. Se trata da afirmação
de que não existe diferença fundamental entre o samsara e Nirvana, impureza e
pureza, ignorância e iluminação. Para o Mahayana, a iluminação, que o caminho
Budista está projetado para despertar, consiste precisamente na realização
dessa perspectiva não-dualista. A validade de dualidades convencionais é negada
porque a natureza última de todos os fenômenos é o vazio, a ausência de
qualquer realidade substancial ou intrínseca e dessa forma no seu vazio todos
os variados fenômenos, aparentemente opostos, que são definidos pela doutrina
Budista tradicional, são finalmente compatíveis: &quot;Todos os dharmas possuem
a mesma natureza, que é a não-natureza&quot;.<o:p></o:p></span></p>

<p class=Normal>
O ensinamento do
Buda tal como encontrado no Cânone em Pali não endossa uma filosofia de
não-dualismo de nenhum tipo, nem, eu adicionaria, pode uma perspectiva
não-dualista ser encontrada implícita nos discursos do Buda. Ao mesmo tempo, no
entanto, eu não diria que os Suttas em Pali propõem o dual<i style='mso-bidi-font-style:
normal'>ismo, </i><span style='mso-bidi-font-style:italic'>ou seja </span>a
proposição da dualidade como uma hipótese metafísica tendo como objetivo a sua
aprovação intelectual. Eu caracterizaria<span style="mso-spacerun: yes"> 
</span>a intenção do Buda no Cânone como, antes de mais nada, pragmática ao
invés de especulativa, embora eu também qualifique isso dizendo que esse
pragmatismo não opera em um vazio filosófico, mas que encontra o seu fundamento
na natureza da realidade tal como o Buda a penetrou na sua iluminação.<span
style="mso-spacerun: yes">  </span>Em contraste com os sistemas não-dualistas,
a abordagem do Buda não objetiva descobrir um princípio unificador por trás ou
suportando a nossa experiência do mundo. Ao invés disso, ele toma o fato
concreto da experiência de vida, com toda a sua ruidosa confusão de contrastes
e tensões, como seu ponto de partida e estrutura e dentro dela ele se empenha
em diagnosticar o problema central que se encontra no núcleo da existência
humana e oferecer um caminho para a sua solução. Portanto, a estrela guia do
caminho Budista não é a unidade final mas a extinção do sofrimento, que traz a
solução do dilema existencial no seu plano mais básico.<o:p></o:p></span></p>

<p class=Normal>
Quando
investigamos a nossa experiência, exatamente da forma como ela se apresenta,
descobrimos que ela está permeada por um número de dualidades com importância
crítica e com profundas implicações na busca espiritual. Os ensinamentos do
Buda, tal como registrados nos Suttas em Pali, fixam a nossa atenção com
firmeza sobre essas dualidades e considera o seu reconhecimento como a base
indispensável para qualquer busca honesta de uma sabedoria libertadora. É
precisamente essa antítese - do benéfico e do prejudicial, sofrimento e
felicidade, sabedoria e ignorância - que fazem a busca pela iluminação e
libertação uma preocupação vital e crucial. <o:p></o:p></span></p>

<p class=Normal>
No topo dos pares
de opostos está a dualidade do condicionado e do Incondicionado: samsara como o
ciclo de repetidos nascimentos e mortes onde tudo é impermanente, sujeito a
mudança e propenso ao sofrimento, e Nibbana como o estado de libertação final,
o que não nasce, não envelhece, imortal. Embora Nibbana, mesmo nos textos mais
antigos, seja definitivamente caracterizado como a realidade última e não
simplesmente como um estado ético ou psicológico, não existe a menor insinuação
de que esta realidade é indistinguível metafisicamente em algum nível profundo
do seu oposto manifesto, samsara. Ao contrário, o repetido ensinamento do Buda
é de que samsara é o domínio do sofrimento governado pelo desejo, raiva e
delusão, onde vertemos lágrimas em maior abundância que as águas dos oceanos,
enquanto que Nibbana é a libertação irreversível do samsara, para ser alcançada
através da destruição do desejo, raiva e delusão e pelo abandono de toda
existência condicionada.<o:p></o:p></span></p>

<p class=Normal>
Dessa forma o
Theravada faz da antítese do samsara e Nibbana o ponto de partida para toda a
busca pela libertação. Ainda mais, ele trata essa antítese como determinante do
objetivo final, que é precisamente a transcendência do samsara e<span
style="mso-spacerun: yes">  </span>atingir a libertação no Nibbana. Onde o
Theravada difere significativamente das escolas do Mahayana, que também iniciam
com a dualidade do samsara e Nirvana, é a sua recusa em considerar essa
polaridade como apenas uma lição preparatória desenhada para aqueles que
possuem faculdades mais grosseiras, para ser eventualmente substituída por
alguma realização mais elevada da não-dualidade. Do ponto de vista dos Suttas
em Pali, mesmo para o Buda e os Arahants o sofrimento e a sua cessação, samsara
e Nibbana, permanecem distintos.<o:p></o:p></span></p>

<p class=Normal>
Aqueles
empenhados na busca espiritual que ainda estão experimentando as diferentes
tradições contemplativas, comumente assumem que o ensinamento espiritual mais
elevado deve ser aquele que apresenta uma unidade metafísica como fundamento
filosófico e objetivo final da busca pela iluminação. Tomando essa premissa
como um axioma, eles podem então concluir que os ensinamentos Budistas em Pali,
com a sua insistência na avaliação sóbria das dualidades, é deficiente ou
provisório, devendo ser completado com uma realização não-dualista. Para
aqueles que possuem essa inclinação, a dissolução das dualidades em uma unidade
última sempre parecerá ser mais profunda e completa.<o:p></o:p></span></p>

<p class=Normal>
No entanto, é
justamente essa premissa que eu questiono. Eu afirmaria, tendo por base o
ensinamento original do Buda, de que a profundidade e a perfeição não precisam
ser compradas a custo das diferenças, de que elas podem ser alcançadas no seu
nível mais elevado, desde que preservem intactas as dualidades e diversidades
tão claramente evidentes na reflexão madura acerca do mundo. Eu adicionaria,
além disso, que o ensinamento que insiste em reconhecer a realidade dual tal
como ela é, será mais satisfatório ao final. A razão porque é mais
satisfatório, apesar de negar a ansiedade que a mente tem de uma unidade
abrangente, é porque ele toma em conta um outro fator que supera em importância
a busca pela unidade. Esse &quot;algo mais&quot; é a necessidade de permanecer
conectado com a realidade.<o:p></o:p></span></p>

<p class=Normal>
Onde penso que o
ensinamento do Buda, tal como preservado pela tradição Theravada, ultrapassa
todas as outras tentativas de resolver o dilema espiritual da humanidade é na sua
persistente recusa em sacrificar a realidade pela unidade. O Dhamma do Buda não
nos direciona para um absoluto que tudo incorpora e onde as tensões da
existência diária se dissolvem em uma unidade metafísica ou um vazio
inescrutável. Ele nos aponta, ao invés disso, para a realidade como o domínio
último da compreensão, diringido-se às coisas como elas realmente são <i
style='mso-bidi-font-style:normal'>(yathabhuta).</i> Acima de tudo, ela nos
aponta para as Quatro Nobres Verdades do sofrimento, a sua origem, a sua
cessação, e o caminho para a cessação, como a proclamação libertadora das
coisas como elas realmente são. Essas quatro verdades, o Buda declara, são <i
style='mso-bidi-font-style:normal'>nobres </i>verdades, e o que as faz nobres é
que precisamente elas são reais, não se desviam, são constantes <i
style='mso-bidi-font-style:normal'>(tatha, avitatha, anannatha).</i> É a nossa
inabilidade em enfrentar a realidade dessas verdades que tem causado a nossa
perambulação por tanto tempo através do longo percurso do samsara. É através da
penetração dessas verdades, exatamente como elas são, que se pode lograr a
verdadeira consumação da busca espiritual, dando um fim ao sofrimento. <o:p></o:p></span></p>

