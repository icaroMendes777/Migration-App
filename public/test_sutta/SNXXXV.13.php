<p class=Normal> Em Savatthi.</p>

<p class=Normal> “Bhikkhus, antes da minha iluminação, quando eu era ainda um Bodisatva não iluminado eu pensei: 'Qual é a gratificação, qual é o perigo, qual é a escapatória no caso do olho? 'Qual é a gratificação, qual é o perigo, qual é a escapatória no caso do ouvido ... nariz ... língua ... corpo ... mente?'</p>

<p class=Normal> "Então, bhikkhus, me ocorreu: 'O prazer e a alegria que surgem na dependência do olho: essa é a gratificação do olho. O olho é impermanente, sofrimento e sujeito à mudança: esse é o perigo do olho. A remoção e o abandono do desejo e cobiça pelo olho: essa é a escapatória do olho. </p>

<p class=Normal> "O prazer e a alegria que surgem na dependência do ouvido ... do nariz ... da língua ... do corpo ... da mente: essa é a gratificação da mente. A  mente é impermanente, sofrimento e sujeita à mudança: esse é o perigo da mente. A remoção e o abandono do desejo e cobiça pela mente: essa é a escapatória da mente. </p>


<p class=Normal> “Enquanto, bhikkhus, não obtive o conhecimento direto da gratificação, do perigo, e da escapatória dessas seis bases internas, como na verdade elas são, não reivindiquei ter despertado para a insuperável perfeita iluminação neste mundo com os seus devas, maras e brahmas, esta população com seus contemplativos e brâmanes, seus príncipes e povo. Mas quando obtive o conhecimento direto da gratificação, do perigo, e da escapatória dessas seis bases internas, como na verdade elas são, reivindiquei ter despertado para a insuperável perfeita iluminação neste mundo com os seus devas, maras e brahmas, esta população com seus contemplativos e brâmanes, seus príncipes e povo. O conhecimento e visão surgiram em mim: ‘A libertação da minha mente é inabalável. Este é o meu último nascimento. Não há mais vir a ser a nenhum estado.’" </p>

