<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Muitas sensações florescem no íntimo,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>originadas da forma que foi vista,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>cobiça e contrariedade também<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>através das quais a mente fica perturbada.<br>

<span lang=PT-BR style='mso-ansi-language:
