<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Tendo conscientizado um objeto mental com a atenção confusa ...<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Para aquele que assim acumula sofrimento<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>é dito que Nibbana está muito distante.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
