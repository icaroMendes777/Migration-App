<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, o
Venerável Malunkyaputta, ficando satisfeito e contente com as palavras do
Abençoado, levantou do seu assento e depois de homenageá-lo, mantendo-o à sua
direita, partiu. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então,
permanecendo só, isolado, diligente, ardente e decidido, em pouco tempo, o
Venerável Malunkyaputta alcançou e permaneceu no objetivo supremo da vida santa
pelo qual membros de um clã deixam a vida em família pela vida santa, tendo
conhecido e realizado por si mesmo no aqui e agora. Ele soube: “O nascimento
foi destruído, a vida santa foi vivida, o que deveria ser feito foi feito, não
há mais vir a ser a nenhum estado.”<span style='mso-spacerun:yes'>  </span>E o
Venerável Malunkyaputta tornou-se mais um dos Arahants. <o:p></o:p></span></p>

