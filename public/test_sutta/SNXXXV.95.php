<p class=Normal><a name=N2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[2] O significado deste parágrafo pode ser interpretado com base no
comentário da seguinte forma: “Com relação ao que é visto houver apenas o
visto” significa que a consciência no olho vê apenas a forma como forma e não
algum outro elemento como sendo permanente, etc. Da mesma forma com relação aos
outros tipos de consciência. “Apenas” indica o limite, significando que a mente
equivale à consciência no olho sem estar afetada pela cobiça, raiva ou delusão
com relação ao objeto que foi contatado e sem adicionar as próprias idéias,
conceitos e proliferações mentais. “Você não estará ‘com aquilo’”: você não
será estimulado ‘com aquela’ cobiça, raiva, ou deludido ‘com aquela’ delusão.
“Então você não estará ‘naquilo’”: não sendo estimulado pela cobiça, etc.,
então você não estará atado, agrilhoado, estabelecido naquilo que é visto,
ouvido, sentido e conscientizado através das respectivas idéias e proliferações
mentais. “Então você não estará aqui, nem além e tampouco entre os dois”: nem
neste mundo e tampouco num mundo além, isto significa a experiência de nibbana
que está além das coisas mundanas. [<a href="#R2">Retorna</a>]<o:p></o:p></span></p>


