<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, o
venerável Malunkyaputta foi até o Abençoado e depois de cumprimentá-lo sentou a
um lado e disse: [<a href="#N1">1</a>] <a name=R1></a>“Venerável senhor, seria
bom se o Abençoado pudesse me ensinar o Dhamma de maneira resumida, de modo
que, tendo ouvido o Dhamma do Abençoado, eu possa permanecer só, isolado,
diligente, ardente e decidido.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E agora,
Malunkyaputta, o que devo dizer aos bhikkhus mais jovens quando um bhikkhu como
você, envelhecido, idoso, pressionado pelos anos, com a idade avançada,
chegando ao último estágio da vida – me pede uma exortação resumida?” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Embora,
venerável senhor, eu esteja velho, envelhecido, com a idade avançada, atribulado pelos anos, avançado na vida, já no
último estágio, peço que o Abençoado me ensine o
Dhamma de forma resumida, que o Iluminado me ensine o Dhamma de forma resumida.
É bem possível que eu entenda as palavras do Abençoado. É bem possível que eu
me torne um herdeiro das palavras do Abençoado.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O que você
pensa, Malunkyaputta, você tem algum desejo, cobiça ou afeição por aquelas
formas perceptíveis pelo olho que não são vistas por você – que você nunca viu antes, 
que você não vê, que você não pensaria poderem ser vistas?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
venerável senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Você tem
algum desejo, cobiça ou afeição por aqueles sons perceptíveis pelo ouvido ...
por aqueles aromas perceptíveis pelo nariz ... por aqueles sabores perceptíveis
pela língua ... por aqueles tangíveis perceptíveis pelo corpo ... por aqueles
objetos mentais perceptíveis pela mente que não são conscientizados por você – que você nunca conscientizou antes, 
que você não conscientiza, que você não pensaria poderem ser conscientizados?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
venerável senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
Malunkyaputta, com relação às coisas vistas, ouvidas, sentidas e
conscientizadas por você: no visto haverá apenas o visto; no ouvido haverá
apenas o ouvido; no sentido haverá apenas o sentido; no conscientizado haverá
apenas o conscientizado. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Quando,
Malunkyaputta, com relação às coisas vistas, ouvidas, sentidas e
conscientizadas por você, no visto houver apenas o visto, no ouvido houver
apenas o ouvido, no sentido houver apenas o sentido, no conscientizado houver
apenas o conscientizado, então, Malunkyaputta, você não estará ‘com aquilo.’
Quando, Malunkyaputta, você não estiver ‘com aquilo,’ então você não estará
‘naquilo.’ Quando, Malunkyaputta, você não estiver ‘naquilo,’ então você não
estará aqui, nem além e tampouco entre os dois. Isso em si mesmo é o fim do
sofrimento.” [<a href="#N2">2</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R2></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Eu entendo em detalhe, venerável senhor, o significado daquilo que foi
dito pelo Abençoado de forma resumida:<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
