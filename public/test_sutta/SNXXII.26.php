<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em Savatthi.
“Bhikkhus, antes da minha iluminação, quando eu ainda era apenas um bodisatva
não iluminado, me ocorreu: ‘Qual é a gratificação, qual é o perigo, qual é a
escapatória no caso da forma? Qual é a gratificação, qual é o perigo, qual é a
escapatória no caso da sensação ... percepção ... formações volitivas ... consciência? <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
bhikkhus, me ocorreu: ‘O prazer e a alegria que surgem na dependência da forma:
essa é a gratificação da forma. Essa forma é impermanente, sofrimento e sujeita
à mudança: esse é o perigo na forma. A remoção e o abandono do desejo e cobiça
pela forma: essa é a escapatória da forma. <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“‘O prazer
e a alegria que surgem na dependência da sensação ... na dependência da
percepção ... na dependência das formações volitivas ... na dependência da consciência:
essa é a gratificação da consciência. Essa consciência é impermanente,
sofrimento e sujeita à mudança: esse é o perigo na consciência. A remoção e o
abandono do desejo e cobiça pela consciência: essa é a escapatória da
consciência.’<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Enquanto,
bhikkhus, não obtive o conhecimento direto de como na verdade são a
gratificação, o perigo e a escapatória no caso dos cinco agregados
influenciados pelo apego, eu não reivindiquei ter despertado para a insuperável
perfeita iluminação neste mundo com os seus devas, maras e brahmas, esta
população com seus contemplativos e brâmanes, seus príncipes e povo. Mas quando
obtive o conhecimento direto de como na verdade são a
gratificação, o perigo e a escapatória no caso dos cinco agregados
influenciados pelo apego, então reivindiquei ter
despertado para a insuperável perfeita iluminação neste mundo com ... seus príncipes
e povo.<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O
conhecimento e visão surgiram em mim: ‘Inabalável é a libertação da minha
mente; este é o meu último nascimento; agora não há mais vir a ser a nenhum
estado.’” <b style='mso-bidi-font-weight:normal'><o:p></o:p></b></span></p>


