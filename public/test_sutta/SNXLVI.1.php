<p class=Normal><a name=N1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[1] <i>Bojjhanga </i>é um composto de <i>boddhi + anga. </i>O comentário
oferece uma explicação dupla: “Os fatores da iluminação são os fatores do
iluminado ou os fatores daquele que busca a iluminação.” O que isso significa?
É através da junção dos estados que consistem da atenção plena, investigação dos fenômenos,
energia, êxtase, tranqüilidade, concentração, equanimidade,
que surgem nos momentos dos caminhos supramundanos, que o nobre discípulo
alcança a iluminação; portanto a junção desses estados é chamada de iluminação.
A definição do Buda (veja o </span><a href="SNXLVI.5.php"><span lang=PT-BR
style='mso-ansi-language:PT-BR'>SN XLVI.5</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>), indica que na sua origem esses fatores foram
concebidos não como fatores que <i>constituem</i> a iluminação, (a posição
assumida pelos comentários), mas como fatores que <i>conduzem</i> à iluminação.
Essa interpretação também conta com o respaldo da origem seqüencial dos fatores
descrita no </span><a href="SNXLVI.3.php"><span lang=PT-BR style='mso-ansi-language:
PT-BR'>SN XLVI.3</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>. </span>[<a
href="#R1">Retorna</a>]</p>


<p class=Normal><a name=N2></a>[2] O afastamento, (<i>viveka</i>), pode ser
de cinco tipos: (i) em relação a um aspecto em particular (temporário, através
da prática de insight); (ii) através da supressão (temporário, ao alcançar os
jhanas); (iii) através da erradicação (permanente, através do caminho
supramundano); (iv) através do apaziguamento ( permanente, através do fruto do
caminho supramundano); (v) através da escapatória (permanente, em Nibbana). A
mesma explicação se aplica ao desapego, (<i>viraga</i>), e à cessação, (<i>nirodha</i>).
O abandono se refere ao abandono das contaminações. [<a href="#R2">Retorna</a>]</p>






