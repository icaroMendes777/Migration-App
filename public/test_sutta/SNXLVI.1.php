<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Da mesma
forma, bhikkhus, é na dependência da virtude, estabelecido na virtude, tendo
perseverado e desenvolvido os sete fatores da iluminação, que um bhikkhu
alcança a majestade e amplitude em relação às qualidades mentais benéficas.[<a
href="#N1">1</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“E como um bhikkhu – na dependência da virtude, estabelecido na virtude,
desenvolve os sete fatores da iluminação? Aqui, bhikkhus, um bhikkhu desenvolve
o fator da iluminação da atenção plena, que tem como base o afastamento,
desapego e cessação que amadurece no abandono. [<a href="#N2">2</a>] <a
name=R2></a>Ele desenvolve o fator da iluminação da investigação dos fenômenos
... Ele desenvolve o fator da iluminação da energia ... Ele desenvolve o
fator da iluminação do êxtase ... Ele desenvolve o fator da iluminação da
tranqüilidade ... Ele desenvolve o fator da iluminação da concentração ... Ele
desenvolve o fator da iluminação da equanimidade, que tem como base o
afastamento, desapego e cessação que amadurece no abandono. Assim é como um
bhikkhu – na dependência da virtude, estabelecido na virtude, tendo perseverado
e desenvolvido os sete fatores da iluminação - alcança a majestade e amplitude
em relação às qualidades mentais benéficas.”<o:p></o:p></span></p>


