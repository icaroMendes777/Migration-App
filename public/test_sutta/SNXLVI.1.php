<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, é na dependência do Himalaia, o rei das montanhas, que as serpentes,
(nagas), crescem de corpo e ganham força. Tendo crescido de corpo e ganhado
força lá, elas descem para os lagos pequenos. Tendo descido para os lagos
pequenos, elas descem para os grandes lagos…pequenos rios…grandes rios…o grande
