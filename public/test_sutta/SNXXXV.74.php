<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. Então, um certo bhikkhu foi até o Abençoado e depois de
cumprimentá-lo sentou a um lado e disse: “Venerável senhor, em tal e qual habitação,
há um certo monge recém ordenado, não muito conhecido, que está enfermo,
aflito, gravemente doente. Seria bom, venerável senhor, se o Abençoado pudesse
visitar esse bhikkhu por compaixão.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, quando
o Abençoado ouviu as palavras “recém ordenado” e “enfermo”, e compreendeu que
se tratava de um bhikkhu não muito conhecido, ele foi visitá-lo. Aquele bhikkhu
viu o Abençoado vindo à distância e ficou agitado na cama. O Abençoado disse:
“Já basta, bhikkhu, não fique agitado na cama. Há assentos preparados, eu
sentarei aqui.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O Abençoado
então, sentou no assento que estava preparado e disse para aquele bhikkhu: “Eu
espero que você esteja melhorando, bhikkhu, espero que você esteja confortável,
espero que as suas sensações de dor estejam diminuindo e não aumentando e
que<span style="mso-spacerun: yes">  </span>a sua diminuição, não o seu
aumento, seja evidente.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
Senhor, eu não estou melhorando, não me sinto confortável. Minhas sensações de
dor estão aumentando, não diminuindo, o seu aumento, não a sua diminuição é
evidente.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu espero
então, bhikkhu, que você não esteja atormentado pelo remorso e arrependimento.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“De fato,
venerável senhor, eu sinto muito remorso e arrependimento.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu espero,
bhikkhu, que você não tenha nenhuma razão para se censurar com relação à
virtude.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu não
tenho nenhuma razão, venerável senhor, para censurar-me com relação à virtude.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Então,
bhikkhu, se você não tem nenhuma razão para se censurar com relação à virtude,
porque você está atormentado pelo remorso e arrependimento?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu
entendo, venerável senhor, que não é com o propósito de purificar a virtude que
o Dhamma tem sido ensinado pelo Abençoado.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se,
bhikkhu, você entende que o Dhamma não tem sido ensinado por mim com o
propósito de purificar a virtude, então por qual propósito você entende ter
sido o Dhamma ensinado por mim?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, eu entendo que o Dhamma tem sido ensinado pelo Abençoado com o
propósito do desaparecimento da cobiça.” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Muito bem,
bhikkhu! É bom que você entenda que o Dhamma tenha sido ensinado por mim com o
propósito do desaparecimento da cobiça. Pois o Dhamma é ensinado por mim com o
propósito do desaparecimento da cobiça. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O que você
pensa, bhikkhu, o olho é permanente ou impermanente?”- “Impermanente, venerável
senhor.” ... “O ouvido ... a mente é permanente ou impermanente?” –
“Impermanente, venerável senhor.” – “Aquilo que é impermanente é sofrimento ou
felicidade?” – “Sofrimento, venerável senhor.” – “Aquilo que é impermanente,
sofrimento e sujeito à mudança é adequado que se considere assim: ‘Isso é meu,
isso sou eu, isso é o meu eu’?” – Não, venerável senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Vendo
dessa forma, o nobre discípulo bem instruído se desencanta com o olho ...
desencanta com a mente. Desencantado ele se torna desapegado. Através do
desapego a sua mente é libertada. Quando ela está libertada surge o
conhecimento: ‘Libertada.’ Ele compreende que: ‘O nascimento foi destruído, a
vida santa foi vivida, o que deveria ser feito foi feito, não há mais vir a ser
a nenhum estado.’” <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o
que o Abençoado disse. O bhikkhu ficou satisfeito e contente com as palavras do
Abençoado. E enquanto essa explanação estava sendo dada o olho imaculado do
Dhamma surgiu naquele bhikkhu: “Tudo que está sujeito </span>ao surgimento está
sujeito à cessação<span lang=PT-BR style='mso-ansi-language:PT-BR'>.” <o:p></o:p></span></p>


