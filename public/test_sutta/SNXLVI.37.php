<p class=Normal> “Bhikkhus, há essas cinco obstruções, obstáculos, corrupções da mente, enfraquecedoras da sabedoria. Quais cinco? O desejo sensual é uma obstrução, um obstáculo, uma corrupção da mente, enfraquecedor da sabedoria. A má vontade é uma obstrução ... A preguiça e o torpor são uma obstrução ... A inquietação e a ansiedade são uma obstrução ... A dúvida é uma obstrução ... enfraquecedora da sabedoria.</p> 
<p class=Normal> “Há, bhikkhus, esses sete fatores da iluminação, que são não-obstruções, não-obstáculos, não-corrupções da mente; quando desenvolvidos e cultivados eles conduzem à realização do fruto do verdadeiro conhecimento e libertação. Quais sete? O fator da iluminação da atenção plena é uma não-obstrução ... O fator da iluminação da equanimidade é uma não-obstrução. Esses são os sete fatores da iluminação que são não-obstruções, não-obstáculos, não-corrupções da mente; quando desenvolvidos e cultivados eles conduzem à realização do fruto do verdadeiro conhecimento e libertação.”</p>


