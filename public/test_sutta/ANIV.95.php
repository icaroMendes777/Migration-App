<p class=Normal> “Bhikkhus, há quatro tipos de pessoas que podem ser encontradas no mundo. Quais quatro? Aquela que não pratica nem para o seu próprio benefício e tampouco para o  benefício dos outros. Aquela que pratica para o benefício dos outros mas não para o seu próprio benefício. Aquela que pratica para o seu próprio benefício mas não o benefício dos outros. Aquela que pratica para o seu próprio benefício e também para o benefício dos outros. </p> 
 
<p class=Normal> “Tal como um pedaço de lenha de uma pira funerária, ardendo em ambas pontas e suja de excremento no meio, não pode ser usado como madeira no vilarejo ou na floresta, eu lhes digo que esse é um símile para o indivíduo que não pratica nem para o seu próprio benefício e tampouco para o benefício dos outros. O indivíduo que pratica para o benefício dos outros mas não para o seu próprio benefício é o superior e mais refinado dos dois. O indivíduo que  pratica para o seu próprio benefício mas não para o benefício dos outros é o superior e mais refinado dos três. O indivíduo que pratica para o seu próprio benefício e também para o benefício dos outros é, entre os quatro, o principal, o cabeça, o mais destacado, o superior e supremo. Da mesma forma como da vaca se obtém o leite, do leite o creme de leite, do creme de leite a manteiga, da manteiga a manteiga líquida, e da manteiga líquida a nata da manteiga líquida; e desses, a nata da manteiga líquida é considerada o principal – assim também, desses quatro, o indivíduo que pratica para o seu próprio benefício e também para o benefício dos outros é o principal, o cabeça, o mais destacado, o superior e supremo.” </p>
 
<p class=Normal> “Essas são os quatro tipos de pessoas que podem ser encontradas no mundo.” </p>

