<p class=Normal> “Bhikkhus, ao dar atenção, com freqüencia, para coisas que são a base para o desejo sensual, o desejo sensual que ainda não surgiu, surge, e o desejo sensual que já surgiu aumenta e expande. Ao dar atenção, com freqüencia, para coisas que são a base para a má vontade, a má vontade que ainda não surgiu, surge, e a má vontade que já surgiu aumenta e expande. Ao dar atenção, com freqüencia, para coisas que são a base para a preguiça e o torpor, a preguiça e o torpor que ainda não surgiram, surgem, e a preguiça e o torpor que já surgiram aumentam e expandem. Ao dar atenção, com freqüencia, para coisas que são a base para a inquietação e a ansiedade, a inquietação e a ansiedade que ainda não surgiram, surgem, e a inquietação e a ansiedade que já surgiram aumentam e expandem. Ao dar atenção, com freqüencia, para coisas que são a base para a dúvida, a dúvida que ainda não surgiu, surge, e a dúvida que já surgiu aumenta e expande.</p>
 
<p class=Normal> “Bhikkhus, ao dar atenção, com freqüência, para coisas que são a base para o fator da iluminação da atenção plena, o fator da iluminação da atenção plena que ainda não surgiu, surge, e o fator da iluminação da atenção plena que já surgiu é consumado através do desenvolvimento ... Ao dar atenção, com freqüência, para coisas que são a base para o fator da iluminação da equanimidade, o fator da iluminação da equanimidade que ainda não surgiu, surge, e o fator da iluminação da equanimidade que já surgiu é consumado através do desenvolvimento.”</p>

