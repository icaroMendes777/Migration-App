<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, ao
amanhecer, vários bhikkhus se vestiram e tomando as suas tigelas e os mantos externos
foram para Savatthi para esmolar alimentos. Então, eles pensaram: “Ainda é
muito cedo para esmolar alimentos em Savatthi. E se nós fossemos até o parque
dos errantes de outras seitas,” e assim eles foram até o parque dos errantes de
outras seitas e ao chegar os cumprimentaram. Quando a conversa amigável e
cortês havia terminado, eles sentaram a um lado. Os errantes lhes disseram:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigos, o
contemplativo Gotama ensina o Dhamma aos seus discípulos deste modo: ‘Venham,
bhikkhus, abandonem os cinco obstáculos, as corrupções da mente que enfraquecem
a sabedoria e desenvolvam corretamente os sete fatores da iluminação.’ Nós
também ensinamos aos nossos discípulos deste modo: ‘Venham, amigos, abandonem
os cinco obstáculos, as corrupções da mente que enfraquecem a sabedoria, e
desenvolvam corretamente os sete fatores da iluminação.’ Qual é então a
distinção, amigos, qual é a variação, qual é a diferença entre o ensinamento do
Dhamma do contemplativo Gotama e o nosso, entre as instruções dele e as nossas?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então,
aqueles bhikkhus nem aprovaram nem desaprovaram as palavras dos errantes. Sem
dizer isto ou aquilo eles se levantaram dos seus assentos e foram embora,
pensando: “Devemos entender o significado dessas palavras na presença do
Abençoado.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Depois de
terem esmolado alimentos em Savatthi e retornado, após a refeição, eles foram
até o Abençoado e depois de cumprimentá-lo sentaram a um lado e relataram o que
havia ocorrido. [O Abençoado disse:]<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
os errantes de outras seitas que assim falam devem ser questionados da seguinte
forma: ‘Amigos, quando a mente se torna letárgica, o desenvolvimento de quais
fatores da iluminação é inoportuno nessa ocasião e o desenvolvimento de quais
fatores é oportuno nessa ocasião? Então, amigos, quando a mente fica excitada,
o desenvolvimento de quais fatores da iluminação é inoportuno nessa ocasião e o
desenvolvimento de quais fatores é oportuno nessa ocasião?’ Sendo questionados
dessa forma, os errantes de outras seitas fracassarão na exposição desse
assunto, e mais ainda, eles se meterão em dificuldades. Por que isso? Porque não
é o território deles. Bhikkhus, eu não vejo ninguém no mundo com os seus
devas, Maras e Brahmas, esta população com os seus contemplativos e brâmanes,
seus príncipes e povo, que pudesse satisfazer a mente com uma resposta a essas
questões, exceto o Tathagata ou os seus discípulos, ou alguém que tenha
aprendido com eles.<o:p></o:p></span></p>



<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-language:PT-BR'>(I. A mente letárgica: inoportuno)<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Numa
ocasião, bhikkhus, em que a mente ficar letárgica, é inoportuno desenvolver o
fator da iluminação da tranqüilidade, o fator da iluminação da concentração e o
fator da iluminação da equanimidade. Por qual razão? Porque a mente está
letárgica, bhikkhus, e é difícil estimulá-la com essas coisas. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Suponham,
bhikkhus, que um homem queira fazer com que um pequeno fogo se incendeie. Se
ele adicionar capim molhado, estrume de vaca molhado e madeira molhada naquele
fogo, espirrar água e espalhar terra sobre ele, seria esse homem capaz de fazer
com que aquele pequeno fogo se incendeie?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
venerável senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Da mesma
maneira, bhikkhus, numa ocasião em que a mente ficar letárgica, é inoportuno
desenvolver o fator da iluminação da tranqüilidade, o fator da iluminação da
concentração e o fator da iluminação da equanimidade. Por qual razão? Porque a
mente está letárgica, bhikkhus, e é difícil estimulá-la com essas coisas.<o:p></o:p></span></p>



<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-language:PT-BR'>(II. A mente letárgica: oportuno)<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Numa
ocasião, bhikkhus, em que a mente ficar letárgica, é oportuno desenvolver o fator
da iluminação da investigação dos fenômenos, o fator da iluminação da energia e
o fator da iluminação do êxtase. Por qual razão? Porque a mente está letárgica,
bhikkhus, e é fácil estimulá-la com essas coisas. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Suponham,
bhikkhus, que um homem queira fazer com que um pequeno fogo se incendeie. Se
ele adicionar capim seco, estrume de vaca seco e madeira seca naquele fogo,
soprá-lo e não espalhar terra sobre ele, seria esse homem capaz de fazer com
que aquele pequeno fogo se incendeie?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim, venerável
senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Da mesma
maneira, bhikkhus, numa ocasião em que a mente ficar letárgica, é oportuno
desenvolver o fator da iluminação da investigação dos fenômenos, o fator da
iluminação da energia e o fator da iluminação do êxtase. Por qual razão? Porque
a mente está letárgica, bhikkhus, e é fácil estimulá-la com essas coisas.<o:p></o:p></span></p>



<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-language:PT-BR'>(III. A mente excitada: inoportuno)<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Numa
ocasião, bhikkhus, em que a mente ficar excitada, é inoportuno desenvolver o
fator da iluminação da investigação dos fenômenos, o fator da iluminação da
energia e o fator da iluminação do êxtase. Por qual razão? Porque a mente está
excitada, bhikkhus, e é difícil acalmá-la com essas coisas.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Suponham,
bhikkhus, que um homem queira apagar uma grande fogueira. Se ele adicionar
capim seco, estrume de vaca seco e madeira seca naquele fogo, soprá-lo e não
espalhar terra sobre ele, seria o homem capaz de apagar aquela grande
fogueira?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Não,
venerável senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Da mesma
maneira, bhikkhus, numa ocasião em que a mente ficar excitada, é inoportuno
desenvolver o fator da iluminação da investigação dos fenômenos, o fator da
iluminação da energia e o fator da iluminação do êxtase. Por qual razão? Porque
a mente está excitada, bhikkhus, e é difícil acalmá-la com essas coisas.<o:p></o:p></span></p>



<p class=Normal align=center style='text-align:center'><span lang=PT-BR
style='mso-ansi-language:PT-BR'>(IV. A mente excitada: oportuno)<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Numa
ocasião, bhikkhus, em que a mente ficar excitada, é oportuno desenvolver o
fator da iluminação da tranqüilidade, o fator da iluminação da concentração e o
fator da iluminação da equanimidade. Por qual razão? Porque a mente está excitada,
bhikkhus, e é fácil acalmá-la com essas coisas.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Suponham,
bhikkhus, que um homem queira apagar uma grande fogueira. Se ele adicionar
capim molhado, estrume de vaca molhado e madeira molhada naquela fogueira,
espirrar água, e espalhar terra sobre ela, seria esse homem capaz de apagar
aquela grande fogueira?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Sim,
venerável senhor.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Da mesma
maneira, bhikkhus, numa ocasião em que a mente ficar excitada, é oportuno
