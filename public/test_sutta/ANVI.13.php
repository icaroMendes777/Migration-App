<p class=Normal> “Bhikkhus, há esses seis elementos que são escapatórias. Quais seis? </p>
 
<p class=Normal> “Há o caso em que um bhikkhu pode dizer, ‘Embora o amor bondade tenha sido desenvolvido, cultivado, feito um veículo, uma base, estabilizado, exercitado e aperfeiçoado, e bem adotado como minha libertação da mente, ainda assim a raiva subjuga a minha mente.’ A ele deve ser dito, ‘Não diga isso. Você não deve falar dessa forma. Não deturpe o Abençoado; não é bom deturpar o Abençoado. O Abençoado não falaria dessa forma. É impossível, não pode ser que – quando o amor bondade foi desenvolvido, cultivado, feito um veículo, uma base, estabilizado, exercitado e aperfeiçoado, e bem adotado como a libertação da mente – que a raiva ainda assim subjugue a mente. Essa possibilidade não existe, pois essa é a escapatória da raiva: o amor bondade como a libertação da mente.’” </p>
 
<p class=Normal> “Além disso, há o caso em que um bhikkhu pode dizer, ‘Embora a compaixão tenha sido desenvolvida, cultivada, feita um veículo, uma base, estabilizada, exercitada e aperfeiçoada, e bem adotada como minha libertação da mente, ainda assim a crueldade subjuga a minha mente.’ A ele deve ser dito, ‘Não diga isso. Você não deve falar dessa forma. Não deturpe o Abençoado; não é bom deturpar o Abençoado. O Abençoado não falaria dessa forma. É impossível, não pode ser que – quando a compaixão foi desenvolvida, cultivada, feita um veículo, uma base, estabilizada, exercitada e aperfeiçoada, e bem adotada como a libertação da mente – que a crueldade ainda assim subjugue a mente. Essa possibilidade não existe, pois essa é a escapatória da crueldade: a compaixão como a libertação da mente.’” </p>
 
<p class=Normal> “Além disso, há o caso em que um bhikkhu pode dizer, ‘Embora a alegria altruísta tenha sido desenvolvida, cultivada, feita um veículo, uma base, estabilizada, exercitada e aperfeiçoada, e bem adotada como minha libertação da mente, ainda assim o descontentamento subjuga a minha mente.’ A ele deve ser dito, ‘Não diga isso. Você não deve falar dessa forma. Não deturpe o Abençoado; não é bom deturpar o Abençoado. O Abençoado não falaria dessa forma. É impossível, não pode ser que – quando a alegria altruísta foi desenvolvida, cultivada, feita um veículo, uma base, estabilizada, exercitada e aperfeiçoada, e bem adotada como a libertação da mente – que o descontentamento ainda assim subjugue a mente. Essa possibilidade não existe, pois essa é a escapatória do descontentamento: a alegria altruísta como a libertação da mente.’” </p>
 
<p class=Normal> “Além disso, há o caso em que um bhikkhu pode dizer, ‘Embora a equanimidade tenha sido desenvolvida, cultivada, feita um veículo, uma base, estabilizada, exercitada e aperfeiçoada, e bem adotada como minha libertação da mente, ainda assim a cobiça subjuga a minha mente.’ A ele deve ser dito, ‘Não diga isso. Você não deve falar dessa forma. Não deturpe o Abençoado; não é bom deturpar o Abençoado. O Abençoado não falaria dessa forma. É impossível, não pode ser que – quando a equanimidade foi desenvolvida, cultivada, feita um veículo, uma base, estabilizada, exercitada e aperfeiçoada, e bem adotada como a libertação da mente – que a cobiça ainda assim subjugue a mente. Essa possibilidade não existe, pois essa é a escapatória da cobiça: a equanimidade como a libertação da mente.’” </p>
 
<p class=Normal> “Além disso, há o caso em que um bhikkhu pode dizer, ‘Embora a ausência de sinais tenha sido desenvolvida, cultivada, feita um veículo, uma base, estabilizada, exercitada e aperfeiçoada, e bem adotada como minha libertação da mente, ainda assim a minha mente é levada pelos sinais.’ A ele deve ser dito, ‘Não diga isso. Você não deve falar dessa forma. Não deturpe o Abençoado; não é bom deturpar o Abençoado. O Abençoado não falaria dessa forma. É impossível, não pode ser que – quando a ausência de sinais foi desenvolvida, cultivada, feita um veículo, uma base, estabilizada, exercitada e aperfeiçoada, e bem adotada como a libertação da mente – que a mente seja levada pelos sinais. Essa possibilidade não existe, pois essa é a escapatória de todos os sinais: a libertação sem sinais da mente.’” [<a href="#N1">1</a>] </p>
 
<p class=Normal><a name="R1"> </a> “Além disso, há o caso em que um bhikkhu pode dizer, ‘Embora “eu sou” não exista mais, e eu não suponha que “eu sou isso,”  ainda assim a flecha da incerteza e perplexidade continua subjugando a minha mente.’  A ele deve ser dito, ‘Não diga isso. Você não deve falar dessa forma. Não deturpe o Abençoado; não é bom deturpar o Abençoado. O Abençoado não falaria dessa forma. É impossível, não pode ser que – quando  “eu sou” não existe mais  e “eu sou isso” não é mais suposto  – que a flecha da incerteza e perplexidade continue subjugando a mente. Essa possibilidade não existe, pois essa é a escapatória da flecha da incerteza e perplexidade: a eliminação da presunção “eu sou”. [<a href="#N2">2</a>] </p>
 
<p class=Normal><a name="R2"> </a> “Esses, bhikkhus, são os seis elementos que são escapatórias.” </p>

