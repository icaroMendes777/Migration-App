<p class=Normal> Em certa ocasião, o venerável Udayi estava em Kamanda no manguezal do brâmane Todeyya. Então um jovem brâmane, um aluno da senhora brâmane do clã Verahaccani foi até o venerável Udayi e ambos se cumprimentaram. Quando a conversa cortês e amigável havia terminado ele sentou a um lado e o venerável Udayi o instruiu, motivou, estimulou e encorajou com um discurso do Dhamma. Então, depois de ter sido instruído, motivado, estimulado e encorajado pelo discurso do Dhamma, o jovem brâmane levantou do seu assento e foi até a senhora brâmane do clã Verahaccani e disse:</p>

<p class=Normal> “Senhora, você deveria saber que o contemplativo Udayi ensina o Dhamma, com o significado e fraseado corretos, que é admirável no início, admirável no meio, admirável no final; e ele revela uma vida santa que é completamente perfeita e imaculada."</p>

<p class=Normal>"Nesse caso, meu jovem, convide o contemplativo Udayi em meu nome para a refeição de amanhã."</p>

<p class=Normal>"Sim senhora", o jovem respondeu e foi até o venerável Udayi e lhe disse: "Que o mestre Udayi concorde em aceitar a refeição de amanhã oferecida pelo nosso reverenciado mestre, a senhora brâmane do clã Verahaccani."</p>

<p class=Normal>O venerável Udayi concordou em silêncio. Então, quando a noite havia terminado, ao amanhecer, o venerável Udayi se vestiu e tomando a tigela e o manto externo, foi para a residência da senhora brâmane do clã Verahaccani, sentando num assento que havia sido preparado. Então, com as próprias mãos, a senhora brâmane serviu e satisfez o venerável Udayi com os vários tipos de boa comida. Em seguida, quando o venerável Udayi havia terminado de comer e retirado a mão da sua tigela, a senhora brâmane calçou as suas sandálias, sentou num assento elevado, cobriu a cabeça e disse: "Contemplativo, ensine o Dhamma." Dizendo, "Haverá uma outra ocasião para isso, irmã," ele levantou do seu assento e partiu. [<a href="#N1">1</a>]</p>

<p class=Normal><a name=R1></a> Uma segunda vez o jovem brâmane foi até o venerável Udayi ... (igual ao primeiro parágrafo até:) ... “Senhora, você deveria saber que o contemplativo Udayi ensina o Dhamma, com o significado e fraseado corretos, que é admirável no início, admirável no meio, admirável no final; e ele revela uma vida santa que é completamente perfeita e imaculada."</p>

<p class=Normal>"Meu jovem, você continua elogiando o contemplativo Udayi, mas quando eu lhe disse: 'Contemplativo, ensine o Dhamma.' Ele disse: 'Haverá uma outra ocasião para isso, irmã,' e levantou do seu assento e partiu."</p>

<p class=Normal>"Senhora, isso aconteceu porque você calçou as sandálias, sentou num assento elevado, cobriu a cabeça e lhe disse: 'Contemplativo, ensine o Dhamma.' Pois esses nobres respeitam e reverenciam o Dhamma."</p>

<p class=Normal>"Nesse caso, meu jovem, convide o contemplativo Udayi em meu nome para a refeição de amanhã."</p>

<p class=Normal>"Sim senhora", o jovem respondeu e foi até o venerável Udayi ... (igual acima até:) ... quando o venerável Udayi havia terminado de comer e retirado a mão da sua tigela, a senhora brâmane tirou as suas sandálias, sentou num assento mais baixo, descobriu a cabeça e disse: "Venerável senhor, o que os arahants afirmam deve existir para que ocorra o prazer e a dor? E o que os arahants afirmam deve não existir para que não ocorra o prazer e a dor?"</p>

<p class=Normal>"Irmã, os arahants afirmam que quando o olho existe ocorre o prazer e a dor, e quando o olho não existe não ocorre o prazer e a dor. Os arahants afirmam que quando o ouvido existe ocorre o prazer e a dor, e quando o ouvido não existe não ocorre o prazer e a dor ... Os arahants afirmam que quando a mente existe ocorre o prazer e a dor, e quando a mente não existe não ocorre o prazer e a dor."</p>

<p class=Normal>Quando isso foi dito a senhora brâmane do clã Verahaccani disse para o venerável Udayi: "Magnífico, Venerável senhor! Magnífico, Venerável senhor! O venerável Udayi esclareceu o Dhamma de várias formas, como se tivesse colocado em pé o que estava de cabeça para baixo, revelasse o que estava escondido, mostrasse o caminho para alguém que estivesse perdido ou segurasse uma lâmpada no escuro para aqueles que possuem visão pudessem ver as formas. Eu busco refúgio no Abençoado, no Dhamma e na Sangha dos bhikkhus. Que o Mestre Udayi se lembre de mim como a discípula leiga que buscou refúgio para o resto da vida.”.</p>


