<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. Então, ao amanhecer, a bhikkhuni Cala se vestiu e tomando a tigela e
o manto externo foi para Savatthi esmolar alimentos. Depois de haver esmolado
alimentos em Savatthi e de haver retornado, após a refeição, ela se dirigiu ao
bosque dos Homens Cegos em busca de isolamento. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então Mara, o Senhor do
Mal, desejando despertar medo, trepidação e terror na bhikkhuni Cala, desejando
que ela abandonasse a sua concentração, se aproximou e disse: “O que você não
aprova, bhikkhuni?” <span style="mso-spacerun: yes"> </span>- “Eu não aprovo o
nascimento, amigo.”<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Porque você não aprova o nascimento?<br>
Uma vez que se nasce, os prazeres sensuais são desfrutados.<br>
Quem a convenceu disso:<br>
'Bhikkhuni, não aprove o nascimento'?”<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>[bhikkhuni
Cala:]<o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Para aquele que nasce existe a morte;<br>
uma vez que se nasce, os sofrimentos são encontrados - <br>
cativeiro, mutilação, aflição - <br>
por conseguinte, o nascimento não deve ser aprovado. <o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“O Buda ensinou o Dhamma,<br>
a superação do nascimento;<br>
para o abandono de todo o sofrimento<br>
ele me estabeleceu na verdade.<span style="mso-spacerun: yes">  </span><o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Quanto aos seres que transitam pela forma,<br>
e aqueles que compartem do sem forma - <br>
não tendo compreendido a cessação,<br>
eles novamente regressam para o ser/existir.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, Mara, o Senhor do Mal,
compreendendo que, “A bhikkhuni Cala sabe quem sou,” triste e desapontado,
desapareceu no mesmo instante. <o:p></o:p></span></p>



