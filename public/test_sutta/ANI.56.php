<p class=Normal> (56) "Bhikkhus, quaisquer qualidades que são prejudiciais, que fazem parte daquilo que é prejudicial, e que pertencem ao que é prejudicial, todas têm a mente como precursor. A mente surge primeiro seguida das qualidades prejudiciais." </p>

<p class=Normal> (57) "Bhikkhus, quaisquer qualidades que são benéficas, que fazem parte daquilo que é benéfico, e que pertencem ao que é benéfico, todas têm a mente como precursor. A mente surge primeiro seguida das qualidades benéficas." </p>

<p class=Normal> (58) "Bhikkhus, eu não vejo nenhuma outra única qualidade, que faz com que qualidades prejudiciais que ainda não surgiram surjam, e as qualidades benéficas que já surgiram declinem, como a negligência. Para quem é negligente, as qualidades prejudiciais que ainda não surgiram surgem, e as qualidades benéficas que já surgiram declinam." </p>

<p class=Normal> (59) "Bhikkhus, eu não vejo nenhuma outra única qualidade, que faz com que qualidades benéficas que ainda não surgiram surjam e as qualidades prejudiciais que já surgiram diminuam, como a diligência. Para quem é diligente as qualidades benéficas que ainda não surgiram surgem, e as qualidades prejudiciais que já surgiram declinam." </p>

<p class=Normal> (60) "Bhikkhus, eu não vejo nenhuma outra única qualidade, que faz com que qualidades prejudiciais que ainda não surgiram surjam, e as qualidades benéficas que já surgiram declinem, como a preguiça. Para quem é preguiçoso, as qualidades prejudiciais que ainda não surgiram surgem, e as qualidades benéficas que já surgiram declinam." </p>

