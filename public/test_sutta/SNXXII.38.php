<p class=Normal> Em Savatthi, O Abençoado então disse para Ananda: </p>

<p class=Normal>"Ananda, se lhe perguntassem: 'Amigo Ananda, quais são as coisas em relação às quais a origem foi discernida, a cessação foi discernida, e uma alteração daquilo que está presente foi discernida? Quais são as coisas em relação às quais a origem será discernida, a cessação será discernida, e uma alteração daquilo que está presente será discernida? Quais são as coisas em relação às quais a origem é discernida, a cessação é discernida, e uma alteração daquilo que está presente é discernida?' - sendo assim perguntado, Ananda, como você responderia?" </p>

<p class=Normal>"Veneraável senhor, se fosse assim perguntado, eu responderia desta forma: "Amigos, com a forma que mudou, passou, cessou, a origem foi discernida, a cessação foi discernida, e uma alteração daquilo que está presente foi discernida. Com a sensação ... percepção ... formações volitivas ... consciência que mudou, passou, cessou, a origem foi discernida, a cessação foi discernida, e uma alteração daquilo que está presente foi discernida. É com relação a essas coisas, amigos, que a origem foi discernida, a cessação foi discernida, e uma alteração daquilo que está presente foi discernida. </p>

<p class=Normal>"Amigos, com a forma que não nasceu, não se tornou manifesta, a origem será discernida, a cessação será discernida, e uma alteração daquilo que está presente será discernida. Com a sensação ... percepção ... formações volitivas ... consciência que não nasceu, não se tornou manifesta, a origem será discernida, a cessação será discernida, e uma alteração daquilo que está presente será discernida. É com relação a essas coisas, amigos, que a origem será discernida, a cessação será discernida, e uma alteração daquilo que está presente será discernida. </p>

<p class=Normal>"Amigos, com a forma que nasceu, que se tornou manifesta, a origem é discernida, a cessação é discernida, e uma alteração daquilo que está presente é discernida. Com a sensação ... percepção ... formações volitivas ... consciência que nasceu, que se tornou manifesta, a origem é discernida, a cessação é discernida, e uma alteração daquilo que está presente é discernida. É com relação a essas coisas, amigos, que a origem é discernida, a cessação é discernida, e uma alteração daquilo que está presente é discernida. </p>

<p class=Normal>"Sendo assim perguntado, venerável senhor, eu responderia dessa forma." </p>

<p class=Normal>"Muito bem, Ananda, muito bem!" </p>

<p class=Normal> (O Buda repete tudo que foi dito por Ananda, concluindo:) </p>

<p class=Normal>"Sendo assim perguntado, Ananda, você deveria responder dessa forma." </p>


