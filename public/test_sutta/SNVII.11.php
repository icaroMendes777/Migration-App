<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Assim ouvi.
Em certa ocasião, o Abençoado estava entre os Magadhas em Dakkhinagiri próximo
ao vilarejo brâmane Ekanala. Agora, naquela ocasião o brâmane Kasi Bharadvaja,
Bharadvaja o Lavrador, tinha quinhentos arados presos às suas respectivas
cangas prontos para a semeadura. [</span><a href="#N1"><span lang=PT-BR
style='mso-ansi-language:PT-BR'>1</span></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>] <a name=R1></a>Então, ao amanhecer, o Abençoado se vestiu e tomando a
tigela e o manto externo foi para o lugar onde o brâmane Kasi Bharadvaja estava
trabalhando.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Agora,
naquela ocasião estava sendo realizada a distribuição de comida pelo brâmane
Kasi Bharadvaja. [</span><a href="#N2"><span lang=PT-BR style='mso-ansi-language:
PT-BR'>2</span></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>] <a
name=R2></a>Então, o Abençoado se aproximou do local onde estava sendo
realizada a distribuição de comida e ficou em pé a um lado. O brâmane Kasi
Bharadvaja viu o Abençoado em pé esperando a esmola de comida e lhe disse:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Contemplativo,
eu lavro e semeio e depois de lavrar e semear, eu como. Você também,
contemplativo, deveria lavrar e semear, então, depois de lavrar e semear, você
irá comer.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu também,
brâmane, lavro e semeio e depois de lavrar e semear, eu como.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas nós
não vemos a canga ou o arado ou a aguilhada ou os bois do Mestre Gotama; e no
entanto o Mestre Gotama diz, ‘Eu também, brâmane, lavro e semeio e depois de
lavrar e semear, eu como.’”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, o
brâmane Kasi Bharadvaja se dirigiu ao Abençoado em versos:<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Você afirma ser um homem que trabalha com o arado,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>mas eu não o vejo arando.<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Se você é um lavrador, responda-me:<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>como devemos entender a sua lavra?”<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[O Abençoado:]<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Fé é a semente, austeridade a chuva, <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>sabedoria a canga e o arado;<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>vergonha é a haste, a mente o atilho da canga,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>atenção plena a lâmina do arado e a aguilhada. [</span><a href="#N3"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>3</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><a name=R3></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>“Com o corpo guardado, com a linguagem
guardada,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>controlado no apetite por comida,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>eu uso a verdade como meu gancho de arrancar as ervas daninhas,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>e a nobreza para destruir o jugo. [</span><a href="#N4"><span
lang=PT-BR style='mso-ansi-language:PT-BR'>4</span></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>]<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><a name=R4></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>“Energia é a minha besta de carga,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>carregando-me para a segurança contra o cativeiro.<span
style='mso-tab-count:1'>     </span><br>

<span lang=PT-BR style='mso-ansi-language:PT-BR'><span
style='mso-tab-count:1'>            </span>Ela segue em frente sem parar<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>para onde, depois de ir, ninguém se entristece.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Dessa forma, esta lavra é feita,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>a que tem o Imortal como seu fruto.<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Ao concluir essa lavra,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>ele estará libertado de todo sofrimento.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Que o
Mestre Gotama coma! Esse nobre é um lavrador, visto que o Mestre Gotama lavra
tendo até mesmo o Imortal como fruto.” <o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Alimento em relação ao qual versos tenham sido declamados <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>não é apropriado que eu coma.<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Esse, brâmane, não é o princípio <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>observado por aqueles que vêem.<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>O Abençoado rejeita esse alimento<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>em relação ao qual versos foram declamados.<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Visto que esse princípio existe, Oh brâmane,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>essa é a regra de comportamento.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'>
<span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Sirva outra comida e bebida para<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>o Perfeito, o Grande Profeta<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>com as impurezas destruídas e o remorso silenciado,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>pois ele é o campo para quem estiver buscando mérito.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isso
foi dito, o brâmane Kasi Bharadvaja disse para o Abençoado:<span
style="mso-spacerun: yes">  </span>“Magnífico, Mestre Gotama! Magnífico, Mestre
Gotama! Mestre Gotama esclareceu o Dhamma de várias formas, como se tivesse
colocado em pé o que estava de cabeça para baixo, revelasse o que estava
escondido, mostrasse o caminho para alguém que estivesse perdido ou segurasse
uma lâmpada no escuro para aqueles que possuem visão pudessem ver as formas.
Nós buscamos refúgio no Mestre Gotama, no Dhamma e na Sangha dos bhikkhus. Que
o Mestre Gotama me aceite como o discípulo leigo que buscou
refúgio para o resto da vida.”<o:p></o:p></span></p>


