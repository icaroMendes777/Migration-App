Buda chegou e ficou num lugar mais elevado não muito distante do brâmane de
modo que eles pudessem conversar. [<a href="#R2">Retorna</a>]<o:p></o:p></span></p>



<p class=Normal><a name=N3></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>[3] Porque o Abençoado começou pela fé? De acordo com o comentário,
porque esse brâmane era inteligente mas carente de fé. Porque a fé é chamada de
“semente”? Porque é o fundamento de todas as qualidades benéficas.
“Austeridade” significa a contenção dos sentidos. “Sabedoria” é insight junto
com a sabedoria do caminho. [<a href="#R3">Retorna</a>]<o:p></o:p></span></p>



