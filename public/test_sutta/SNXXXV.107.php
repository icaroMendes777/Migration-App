<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em Savathi.
“Bhikkhus, eu ensinarei para vocês a origem do mundo e o fim do mundo. Ouçam e
prestem muita atenção àquilo que eu vou dizer.” – “Sim, venerável senhor,” os
bhikkhus responderam. O Abençoado disse o seguinte: <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual é a
origem do mundo? Na dependência do olho e das formas a consciência no olho
surge. O encontro dos três é o contato. Do contato como condição, a sensação
[surge]. Da sensação como condição, o desejo. Do desejo como condição, o apego.
Do apego como condição, o ser/existir. Do ser/existir como condição, o
nascimento. Do nascimento como condição, então o envelhecimento e morte,
lamentação, dor, angústia e desespero surgem. Essa é a origem do mundo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Na
dependência do ouvido e dos sons a consciência no ouvido surge. O encontro dos
três é o contato ... Na dependência do nariz e dos aromas a consciência no
nariz surge. O encontro dos três é o contato ... Na dependência da língua e dos
sabores a consciência na língua surge. O encontro dos três é o contato ... Na
dependência do corpo e dos tangíveis a consciência no corpo surge. O
encontro dos três é o contato ... Na dependência da mente e dos objetos mentais
a consciência na mente surge. O encontro dos três é o contato. Do contato como
condição, a sensação [surge]. Da sensação como condição, o desejo. Do desejo
como condição, o apego. Do apego como condição, o ser/existir. Do ser/existir
como condição, o nascimento. Do nascimento como condição, então o
envelhecimento e morte, lamentação, dor, angústia e desespero surgem. Essa é a
origem do mundo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E qual é o
fim do mundo? Na dependência do olho e das formas a consciência no olho surge.
O encontro dos três é o contato. Do contato como condição, a sensação [surge].
Da sensação como condição, o desejo. Agora, com o desaparecimento e cessação
sem deixar vestígios desse mesmo desejo, cessa o apego. Da cessação do apego,
cessa o ser/existir. Da cessação do ser/existir, cessa o nascimento. Da
cessação do nascimento, então o envelhecimento e morte, lamentação, dor,
angústia e desespero, tudo cessa. Essa é a cessação de toda essa massa de
sofrimento. Esse é o fim do mundo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Na
dependência do ouvido e dos sons a consciência no ouvido surge. O encontro dos
três é o contato ... Na dependência do nariz e dos aromas a consciência no
nariz surge. O encontro dos três é o contato ... Na dependência da língua e dos
sabores a consciência na língua surge. O encontro dos três é o contato ... Na
dependência do corpo e dos tangíveis a consciência no corpo surge. O encontro
dos três é o contato ... Na dependência da mente e dos objetos mentais a
consciência na mente surge. O encontro dos três é o contato. Do contato como
condição, a sensação [surge]. Da sensação como condição, o desejo. Agora, com o
desaparecimento e cessação sem deixar vestígios desse mesmo desejo, cessa o
apego. Da cessação do apego, cessa o ser/existir. Da cessação do ser/existir,
cessa o nascimento. Da cessação do nascimento, então o envelhecimento e morte,
lamentação, dor, angústia e desespero, tudo cessa. Essa é a cessação de toda
essa massa de sofrimento. Esse é o fim do mundo.<a name=T2></a><o:p></o:p></span></p>


