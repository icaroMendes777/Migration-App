<p class=Normal> “Bhikkhus, há esses quatro tipos de prática Quais quatro? (1) A prática que é dolorosa, com conhecimento direto lento. (2) A prática que é dolorosa, com conhecimento direto rápido. (3) A prática que é prazerosa, com conhecimento direto lento. (4) A prática que é prazerosa, com conhecimento direto rápido. </p>

<p class=Normal><a name=R1></a> (1) "Como, bhikkhus, é a prática que é dolorosa, com conhecimento direto lento? Aqui, um bhikkhu permanece contemplando as coisas repulsivas no corpo, percebendo as coisas repulsivas na comida, percebendo o não-deleite com tudo no mundo, contemplando a impermanência em todos os fenômenos condicionados; e ele tem a percepção da morte bem estabelecida internamente. Ele permanece na dependência destes cinco poderes de um treinando: o poder da fé, o poder da vergonha moral, o poder do temor moral, o poder da energia, o poder da sabedoria. Estas cinco faculdades nele surgem com debilidade: as faculdades da fé, energia, atenção plena, concentração, e sabedoria. Porque essas faculdades são débeis, ele realiza com lentidão a condição próxima para a destruição das impurezas. [<a href="#N1">1</a>] Essa é chamada a prática que é dolorosa, com conhecimento direto lento. </p>

<p class=Normal> (2) "Como é a prática que é dolorosa, com conhecimento direto rápido? Aqui, um bhikkhu permanece contemplando as coisas repulsivas no corpo ... e ele tem a percepção da morte bem estabelecida internamente. Ele permanece na dependência destes cinco poderes de um treinando: o poder da fé, o poder da vergonha moral, o poder do temor moral, o poder da energia, o poder da sabedoria. Estas cinco faculdades nele surgem com proeminência: as faculdades da fé, energia, atenção plena, concentração, e sabedoria. Porque essas faculdades são proeminentes, ele realiza com rapidez a condição próxima para a destruição das impurezas. Essa é chamada a prática que é dolorosa, com conhecimento direto rápido. </p>

<p class=Normal> (3) "Como é a prática que é prazerosa, com conhecimento direto lento? Aqui, afastado dos prazeres sensuais, afastado das qualidades não hábeis, um bhikkhu entra e permanece no primeiro jhana, que é caracterizado pelo pensamento aplicado e sustentado, com o êxtase e felicidade nascidos do afastamento ... entra e permanece no segundo jhana ... entra e permanece no terceiro jhana ... entra e permanece no quarto jhana, que possui nem felicidade nem sofrimento, com a atenção plena e a equanimidade purificadas. Ele permanece na dependência destes cinco poderes de um treinando: o poder da fé, o poder da vergonha moral, o poder do temor moral, o poder da energia, o poder da sabedoria. Estas cinco faculdades nele surgem com debilidade: as faculdades da fé, energia, atenção plena, concentração, e sabedoria. Porque essas faculdades são débeis, ele realiza com lentidão a condição próxima para a destruição das impurezas. Essa é chamada a prática que é prazerosa, com conhecimento direto lento. </p>

<p class=Normal> (4) "Como é a prática que é prazerosa, com conhecimento direto rápido? Aqui, afastado dos prazeres sensuais, afastado das qualidades não hábeis, um bhikkhu entra e permanece no primeiro jhana, que é caracterizado pelo pensamento aplicado e sustentado, com o êxtase e felicidade nascidos do afastamento ... entra e permanece no segundo jhana ... entra e permanece no terceiro jhana ... entra e permanece no quarto jhana, que possui nem felicidade nem sofrimento, com a atenção plena e a equanimidade purificadas. Ele permanece na dependência destes cinco poderes de um treinando: o poder da fé, o poder da vergonha moral, o poder do temor moral, o poder da energia, o poder da sabedoria. Estas cinco faculdades nele surgem com proeminência: as faculdades da fé, energia, atenção plena, concentração, e sabedoria. Porque essas faculdades são proeminentes, ele realiza com rapidez a condição próxima para a destruição das impurezas. Essa é chamada a prática que é prazerosa, com conhecimento direto rápido. </p>

<p class=Normal> "Esses, bhikkhus são os quatro tipos de prática."</p>
