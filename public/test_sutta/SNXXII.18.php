<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, a forma é impermanente. A causa e condição para o
surgimento da forma também é impermanente. Como a forma se origina daquilo que
é impermanente, como poderia ela ser permanente?<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“A sensação
é impermanente ... A percepção é impermanente ... As formações volitivas são
impermanentes ... A consciência é impermanente. A causa e condição para o
surgimento da consciência também é impermanente. Como a consciência se origina
daquilo que é impermanente, como poderia ela ser permanente?<o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Vendo
dessa forma, bhikkhus, um nobre discípulo bem instruído se desencanta com a
forma, se desencanta com a sensação, se desencanta com a percepção, se
desencanta com as formações volitivas, se desencanta com a consciência. Desencantado, ele
se torna desapegado. Através do desapego a sua mente é libertada. Quando ela
está libertada surge o conhecimento: ‘Libertada.’ Ele compreende que: ‘O nascimento
foi destruído, a vida santa foi vivida, o que deveria ser feito foi feito, não
há mais vir a ser a nenhum estado.’”<b style='mso-bidi-font-weight:normal'><o:p></o:p></b></span></p>


