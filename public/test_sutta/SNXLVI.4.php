</span>em Savatthi no Bosque de Jeta, no Parque de Anathapindika. Lá ele se
dirigiu aos monges desta forma: “Amigos bhikkhus” – “Amigo,” eles responderam.
O Venerável Sariputta disse o seguinte: <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigos, há
esses sete fatores da iluminação. Quais sete? O fator da iluminação da atenção
plena, o fator da iluminação da investigação dos fenômenos, o fator da
iluminação da energia, o fator da iluminação do êxtase, o fator da iluminação
da tranqüilidade, o fator da iluminação da concentração, o fator da iluminação
da equanimidade. Esses são os sete fatores da iluminação. [<a href="#N1">1</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Em qualquer um desses sete fatores da iluminação que eu quiser
permanecer durante a manhã, eu permaneço. Em qualquer um desses sete fatores
que eu quiser permanecer durante o meio do dia, eu permaneço. Em qualquer um
desses sete fatores que eu quiser permanecer durante<span style="mso-spacerun:
yes">  </span>noite, eu permaneço.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se,
amigos, me ocorrer, ‘[Que seja] o fator da iluminação da atenção plena,’ então
me ocorre, ‘É imensurável’; então me ocorre, ‘Está completamente aperfeiçoado.’
Enquanto ele persiste, eu compreendo, ‘Ele persiste.’ Se ele diminuir em mim,
eu compreendo, ‘Ele diminuiu em mim por alguma razão em particular.’ <o:p></o:p></span></p>

<p class=Normal>...</p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se,
amigos, me ocorrer, ‘[Que seja] o fator da iluminação da equanimidade,’ então
me ocorre, ‘É imensurável’; então me ocorre, ‘Está completamente aperfeiçoado.’
Enquanto ele persiste, eu compreendo, ‘Ele persiste.’ Se ele diminuir em mim,
eu compreendo, ‘Ele diminuiu em mim por alguma razão em particular.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>&quot;Suponham
que um rei ou o ministro de um rei tivesse um baú cheio de várias roupas
coloridas. Durante a manhã, ele poderia vestir qualquer roupa que ele
desejasse; durante o meio do dia, ele poderia vestir qualquer roupa que ele
desejasse; durante a noite, ele poderia vestir qualquer roupa que ele
desejasse. Da mesma forma, amigos, em qualquer um desses sete fatores da
iluminação que eu quiser permanecer durante a manhã ... durante o meio do dia
... durante a noite, eu permaneço nesse fator da iluminação durante a noite.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se,
