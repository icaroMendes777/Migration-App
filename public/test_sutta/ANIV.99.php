<p class=Normal> “Bhikkhus, há quatro tipos de pessoas que podem ser encontradas no mundo. Quais quatro? (1) Aquela que pratica para o seu próprio benefício mas não para o benefício dos outros. (2) Aquela que pratica para o benefício dos outros mas não para o seu próprio benefício. (3) Aquela que não pratica nem para o seu próprio benefício e tampouco para o benefício dos outros. (4) Aquela que pratica para o seu próprio benefício e também para o benefício dos outros. </p>

<p class=Normal> (1) "E como, bhikkhus, é a pessoa que pratica para o seu próprio benefício mas não para o benefício dos outros? Aqui, alguém, ele mesmo se abstêm de matar seres vivos mas não encoraja os outros a se absterem de matar seres vivos. Ele mesmo se abstém de tomar aquilo que não for dado mas não encoraja os outros a se absterem de tomar aquilo que não for dado. Ele mesmo se abstém do comportamento sexual impróprio mas não encoraja os outros a se absterem do comportamento sexual impróprio. Ele mesmo se abstém da linguagem mentirosa mas não encoraja os outros a se absterem da linguagem mentirosa. Ele mesmo se abstém do vinho, álcool e outros embriagantes que causam a negligência mas não encoraja os outros a se absterem do vinho, álcool e outros embriagantes que causam a negligência. Dessa forma a pessoa pratica para o seu próprio benefício mas não para o benefício dos outros. </p>

<p class=Normal> (2) "E como é a pessoa que pratica para o benefício dos outros mas não para o seu próprio benefício? Aqui, alguém, ele mesmo não se abstêm de matar seres vivos mas ele encoraja os outros a se absterem de matar seres vivos  ... Ele mesmo não se abstém do vinho, álcool e outros embriagantes que causam a negligência mas ele encoraja os outros a se absterem do vinho, álcool e outros embriagantes que causam a negligência. Dessa forma a pessoa pratica para o benefício dos outros mas não para o seu próprio benefício. </p>

<p class=Normal> (3) "E como é a pessoa que não pratica nem para o seu próprio benefício e tampouco para o benefício dos outros? Aqui, alguém, ele mesmo não se abstêm de matar seres vivos e ele não encoraja os outros a se absterem de matar seres vivos  ... Ele mesmo não se abstém do vinho, álcool e outros embriagantes que causam a negligência e ele não encoraja os outros a se absterem do vinho, álcool e outros embriagantes que causam a negligência. Dessa forma a pessoa não pratica nem para o seu próprio benefício e tampouco para o benefício dos outros. </p>

<p class=Normal> (4) "E como é a pessoa que pratica para o seu próprio benefício e também para o benefício dos outros? Aqui, alguém, ele mesmo se abstêm de matar seres vivos e ele  encoraja os outros a se absterem de matar seres vivos  ... Ele mesmo se abstém do vinho, álcool e outros embriagantes que causam a negligência e ele encoraja os outros a se absterem do vinho, álcool e outros embriagantes que causam a negligência. Dessa forma a pessoa pratica para o seu próprio benefício e para o benefício dos outros. </p>

<p class=Normal> “Esses são os quatro tipos de pessoas que podem ser encontradas no mundo.” </p>

