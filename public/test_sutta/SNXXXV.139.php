<p class=Normal> “Bhikkhus, tudo aquilo que não é seu, abandonem-no. Ao abandoná-lo, isso irá conduzir ao seu bem-estar e felicidade. E o que, bhikkhus, não é seu? As formas não são suas, abandonem-nas. Ao abandoná-las, isso irá conduzir ao seu bem-estar e felicidade. Os sons não são seus, ... os objetos mentais não são seus, abandonem-nos. Ao abandoná-los, isso irá conduzir ao seu bem-estar e felicidade.  </p>

<p class=Normal> “Bhikkhus, o que vocês pensam? Se as pessoas levassem embora a grama, gravetos, galhos e folhas deste bosque de Jeta, ou se os queimassem, ou fizessem com eles o que desejassem, vocês pensariam: ‘As pessoas estão nos levando ou estão nos queimando ou estão fazendo conosco o que desejam?’ – “Não, venerável senhor. Porque não? Porque isso não é nem nosso eu, nem pertence ao nosso eu.” – “Da mesma forma, bhikkhus, tudo aquilo que não é seu, abandonem-no. Ao abandoná-lo, isso irá conduzir ao seu bem-estar e felicidade. E o que, bhikkhus, não é seu? O olho não é seu, abandonem-no. Ao abandoná-lo, isso irá conduzir ao seu bem-estar e felicidade. O ouvido não é seu, ... a mente não é sua, abandonem-na. Ao abandoná-la, isso irá conduzir ao seu bem-estar e felicidade." </p>

