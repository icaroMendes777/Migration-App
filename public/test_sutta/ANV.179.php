<p class=Normal> Então o chefe de família Anathapindika, acompanhado de quinhentos discípulos leigos, foi até o Abençoado e depois de cumprimentá-lo sentou a um lado. O Abençoado então disse para o venerável Sariputta: </p>

<p class=Normal> "Você deve saber, Sariputta, que qualquer chefe de família vestido de branco cujas ações estão controladas por cinco regras de treinamento e que obtém de acordo com sua vontade quatro permanências prazerosas visíveis que correspondem à mente superior, pode, se assim quiser, declarar de si mesmo: 'Eu dei um fim ao inferno, ao mundo animal, ao mundo dos fantasmas famintos, eu dei um fim aos estados de privação, ao destino infeliz, aos reinos inferiores; eu entrei na correnteza, não mais destinado aos mundos inferiores, com o destino fixo, tenho a iluminação como destino.'</p>

<p class=Normal> (1) "Quais são as cinco regras de treinamento através das quais as suas ações são controladas? Aqui, Sariputta, um nobre discípulo se abstém de matar, se abstém de roubar, se abstém da conduta sexual imprópria, se abstém de mentir, se abstém de tomar vinho, álcool e outros embriagantes que causam negligência. As suas ações são controladas por essas cinco regras de treinamento. </p>

<p class=Normal> "Quais são as quatro permanências prazerosas visíveis que correspondem à mente superior que ele obtém de acordo com sua vontade? </p>

<p class=Normal> (2) "Aqui, o nobre discípulo possui perfeita claridade, serenidade e confiança no Buda assim: 'O Abençoado é um arahant, perfeitamente iluminado, consumado no verdadeiro conhecimento e conduta, bem-aventurado, conhecedor dos mundos, um líder insuperável de pessoas preparadas para serem treinadas, mestre de devas e humanos, desperto, sublime.' Essa é a primeira permanência prazerosa visível que corresponde à mente superior, que ele realizou para a purificação da mente impura, para a limpeza da mente poluída. </p>

<p class=Normal> (3) "Outra vez, o nobre discípulo possui perfeita claridade, serenidade e confiança no Dhamma assim: 'O Dhamma é bem proclamado pelo Abençoado, visível no aqui e agora, com efeito imediato, que convida ao exame, que conduz para adiante, para ser experimentado pelos sábios por eles mesmos.' Essa é a segunda permanência prazerosa visível que corresponde à mente superior, que ele realizou para a purificação da mente impura, para a limpeza da mente poluída. </p>

<p class=Normal> (4) "Outra vez, o nobre discípulo possui perfeita claridade, serenidade e confiança na Sangha assim: 'A Sangha dos discípulos do Abençoado pratica o bom caminho, pratica o caminho reto, pratica o caminho verdadeiro, pratica o caminho adequado, isto é, os quatro pares de pessoas, os oito tipos de indivíduos; esta Sangha dos discípulos do Abençoado é merecedora de dádivas, merecedora de hospitalidade, merecedora de oferendas, merecedora de saudações com reverência, um campo inigualável de mérito para o mundo.' Essa é a terceira permanência prazerosa visível que corresponde à mente superior, que ele realizou para a purificação da mente impura, para a limpeza da mente poluída. </p>

<p class=Normal> (5) "Outra vez, o nobre discípulo possui as virtudes apreciadas pelos nobres – intactas, não-laceradas, imaculadas, não-matizadas, libertadoras, elogiadas pelos sábios, desapegadas, que conduzem à concentração. Essa é a quarta permanência prazerosa visível que corresponde à mente superior, que ele realizou para a purificação da mente impura, para a limpeza da mente poluída. </p>

<p class=Normal> "Essas são as  quatro permanências prazerosas visíveis que correspondem à mente superior que ele obtém de acordo com sua vontade. </p>

<p class=Normal> "Você deve saber, Sariputta, que qualquer chefe de família vestido de branco cujas ações estão controladas por essas cinco regras de treinamento e que obtém de acordo com sua vontade quatro permanências prazerosas visíveis que correspondem à mente superior, pode, se assim quiser, declarar de si mesmo: 'Eu dei um fim ao inferno, ao mundo animal, ao mundo dos fantasmas famintos, eu dei um fim aos estados de privação, ao destino infeliz, aos reinos inferiores; eu entrei na correnteza, não mais destinado aos mundos inferiores, com o destino fixo, tenho a iluminação como destino.'"</p>

<p class=Normal> Tendo visto o perigo no inferno, <br>
ele deve evitar as ações ruins; <br>
tendo se comprometido com o nobre Dhamma, <br>
o sábio deve evitá-las. </p>

<p class=Normal> Ao extremo da sua capacidade, <br>
ele não deve ferir seres vivos, <br>
com conhecimento não deve mentir; <br>
não deve tomar o que não tenha sido dado. </p>

<p class=Normal> Deve estar contente com a sua esposa, <br>
deve evitar as esposas dos outros. <br>
Não deve beber vinho e álcool, <br>
que causam confusão mental. </p>

<p class=Normal> Ele deve se recordar do Buda<br>
e ponderar sobre o Dhamma. <br>
Deve desenvolver uma mente benevolente, <br>
que conduz ao mundo dos devas. </p>

<p class=Normal> Quando há disponibilidade de coisas para dar, <br>
por quem quer e deseja méritos,<br>
uma oferenda é vasta<br>
se dada para os nobres. </p>

<p class=Normal> Eu descreverei os nobres, <br>
Sariputta, ouça. <br>
Entre o gado de qualquer tipo, <br>
quer seja preto, branco, vermelho ou dourado, <br>
matizado, uniforme ou da tonalidade do pombo, <br>
nasce o touro domado, <br>
que é capaz de suportar o fardo, <br>
possuindo força, progredindo com bom passo. <br>
Somente ele leva o fardo; <br>
sem se preocupar com a sua cor. <br>
Assim também entre os seres humanos<br>
há vários tipos de nascimento -<br>
entre khattiyas, brâmanes, vessas, <br>
suddas, candalas, ou lixeiros -<br>
entre as pessoas de qualquer tipo <br>
nasce a pessoa domada com bons modos; <br>
firme no Dhamma, com a conduta virtuosa, <br>
linguagem veraz, dotada de vergonha de cometer transgressões; <br>
aquela que abandonou o nascimento e morte, <br>
consumada na vida espiritual, <br>
com o fardo deposto, desapegada, <br>
cumpriu sua tarefa, livre das impurezas; <br>
que superou todas as coisas do mundo<br>
e através do desapego realizou nibbana: <br>
uma oferenda de fato é vasta<br>
quando plantada nesse campo imaculado. </p>

<p class=Normal> Tolos desprovidos de entendimento, <br>
embotados, sem aprendizado, <br>
não se preocupam com os nobres<br>
dando as suas oferendas aos forasteiros. <br>
Aqueles que no entanto se preocupam com os nobres, <br>
os sábios estimados, <br>
e aqueles cuja fé no Abençoado<br>
é profunda e bem estabelecida, <br>
vão para o mundo dos devas<br>
ou aqui nascem numa boa família. <br>
Avançando em estágios sucessivos, <br>
esses sábios realizam nibbana.  </p>
