<p class=Normal> “Bhikkhus, quando existe o quê, através do apego a quê, devido à adesão a quê, ocorre o pensamento: 'Eu sou superior,' ou 'Eu sou igual,' ou 'eu sou inferior'?"</p>

<p class=Normal> “Venerável senhor, os nossos ensinamentos têm o Abençoado como origem, como guia e como refúgio. Seria bom se o Abençoado pudesse explicar o significado dessas palavras. Tendo ouvido do Abençoado, os bhikkhus o recordarão.” </p>

<p class=Normal> “Quando existe o olho, bhikkhus, através do apego ao olho, devido à adesão ao olho, ocorre o pensamento: 'Eu sou superior,' ou 'Eu sou igual,' ou 'eu sou inferior'. Quando existe o ouvido ... Quando existe a mente, através do apego à mente, devido à adesão à mente, ocorre o pensamento: 'Eu sou superior,' ou 'Eu sou igual,' ou 'eu sou inferior'. </p>

<p class=Normal> “O que vocês pensam, bhikkhus, o olho é permanente ou impermanente?” </p>

<p class=Normal> “Impermanente, senhor.” </p>

<p class=Normal> “E aquilo que é impermanente é sofrimento ou felicidade?” </p>

<p class=Normal> “Sofrimento, senhor.” </p>

<p class=Normal> “Mas sem o apego àquilo que é impermanente, sofrimento e sujeito a mudanças, ocorreria o pensamento: 'Eu sou superior,' ou 'Eu sou igual,' ou 'eu sou inferior'?”</p>

<p class=Normal> “Não, venerável senhor.” </p>

<p class=Normal> “O ouvido ... a mente é permanente ou impermanente?” </p>

<p class=Normal> “Impermanente, senhor.” </p>

<p class=Normal> “E aquilo que é impermanente é sofrimento ou felicidade?” </p>

<p class=Normal> “Sofrimento, senhor.” </p>

<p class=Normal> “Mas sem o apego àquilo que é impermanente, sofrimento e sujeito a mudanças, ocorreria o pensamento: 'Eu sou superior,' ou 'Eu sou igual,' ou 'eu sou inferior'?”</p>

<p class=Normal> “Não, venerável senhor.” </p>

<p class=Normal> “Vendo dessa forma, o nobre discípulo bem instruído se desencanta com o olho ... com a mente. Desencantado ele se torna desapegado. Através do desapego a sua mente é libertada. Quando ela está libertada surge o conhecimento: ‘Libertada.’ Ele compreende que: ‘O nascimento foi destruído, a vida santa foi vivida, o que deveria ser feito foi feito, não há mais vir a ser a nenhum estado.’” </p>

