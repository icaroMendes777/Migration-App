<p class=Normal> Em Savatthi. “Bhikkhus, no passado os Dasarahas possuíam um grande
tambor denominado ‘Convocador’. Sempre que o convocador apresentava uma
rachadura os Dasarahas introduziam mais uma cavilha nele até
que o corpo original de madeira do convocador havia desaparecido e restava
somente um aglomerado de cavilhas. [<a href="#N1">1</a>]<o:p></o:p></span></p>


<p class=Normal><a name=R1></a>  “Assim também, bhikkhus, o mesmo ocorrerá com os
bhikkhus no futuro. Quando os discursos ditos pelo Tathagata estão sendo recitados, que são profundos, profundos em significado, que transcendem o mundo, conectados com o vazio, os bhikkhus não querem ouvi-los, não dão atenção, ou aplicam as suas mentes para compreendê-los; eles não acham que esses ensinamentos devem ser estudados e aprendidos. Mas quando outros discursos estão sendo recitados que são mera poesia composta por poetas, belos em palavras e frases, criados por forasteiros, ditos por discípulos, eles querem ouvi-los, lhes dão atenção, e aplicam as suas mentes para compreendê-los; eles pensam que esses ensinamentos deve ser estudados e aprendidos.<o:p></o:p></span></p>


<p class=Normal> “Dessa forma ocorrerá o desaparecimento dos discursos que são as
palavras do Tathagata - profundos, profundos no seu significado,
transcendentes, que tratam do vazio.<o:p></o:p></span></p>


<p class=Normal> “Portanto, bhikkhus, assim vocês deveriam praticar: ‘Quando aqueles
discursos que são as palavras do Tathagata – profundos, profundos no seu
significado, transcendentes, que tratam do vazio – forem recitados, nós daremos
ouvidos, empenharemos as nossas mentes no seu aprendizado, consideraremos que o
entendimento e o conhecimento a fundo desses ensinamentos valem a pena. Assim é
como vocês devem praticar.”<o:p></o:p></span></p>


