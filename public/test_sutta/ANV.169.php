<p class=Normal> Então o venerável Ananda foi até o venerável Sariputta e depois de cumprimentá-lo sentou a um lado e disse: </p>

<p class=Normal> "De que modo, amigo Sariputta, um bhikkhu possui rápida compreensão com relação aos ensinamentos benéficos, aquele que compreende bem o que foi estudado, que aprende muito, e que não esquece aquilo que aprendeu?" </p>

<p class=Normal> "O venerável Ananda é estudado, então que ele mesmo responda essa questão."

<p class=Normal> "Então ouça, amigo Sariputta, e preste atenção. Eu falarei." </p>

<p class=Normal> "Sim, amigo," o venerável Sariputta respondeu. O venerável Ananda disse o seguinte: </p>

<p class=Normal> "Aqui, amigo Sariputta, um bhikkhu é hábil no significado, hábil no Dhamma, hábil na linguagem, hábil no fraseado, e hábil na sequência. Desse modo, amigo Sariputta, ele possui rápida compreensão com relação aos ensinamentos benéficos, aquele que compreende bem o que foi estudado, que aprende muito, e que não esquece aquilo que aprendeu." </p>

<p class=Normal> "É maravilhoso e admirável, amigo, quão bem isso foi dito pelo venerável Ananda! Nós consideramos que o venerável Ananda possui essas cinco qualidades. 'O venerável Ananda é hábil no significado, hábil no Dhamma, hábil na linguagem, hábil no fraseado, e hábil na sequência.'" </p>

