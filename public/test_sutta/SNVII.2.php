<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em certa
ocasião, o Abençoado estava<span style='mso-spacerun:yes'>  </span>em Rajagaha,
no Bambual, no Santuário dos Esquilos. O brâmane Akkosaka Bharadvaja, Bharadvaja
o Abusivo, ouviu: “Dizem que um brâmane do clã Bharadvaja deixou a vida em
família e seguiu a vida santa sob o contemplativo Gotama.” Furioso e desgostoso
ele foi até o Abençoado e abusou dele e o insultou com palavras grosseiras e
rudes.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando ele
terminou de falar, o Abençoado lhe disse: “O que você pensa, brâmane? Os seus
amigos e colegas, pares e parentes, bem como convidados vêm visitá-lo?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Algumas
vezes eles vêm me visitar, Mestre Gotama.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Você então
lhes oferece algo de comer ou uma refeição ou um aperitivo?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Algumas
vezes ofereço, Mestre Gotama.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas se
eles não aceitarem isso de você, a quem pertence a comida?”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Se eles
não aceitarem aquilo de mim, então a comida ainda nos pertence.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Da mesma
forma, brâmane, nós – que não abusamos de ninguém, que não berramos com
ninguém, que não censuramos ninguém com grosserias – recusamo-nos a aceitar de
você o abuso e insulto e censura que você fez desencadear sobre nós. Isso ainda
lhe pertence, brâmane! Isso ainda lhe pertence, brâmane!<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Brâmane,
aquele que abusa quem o abusa, que insulta quem o insulta, que censura com
grosserias quem o censura com grosserias – diz-se que ele comparte da refeição,
realizou uma permuta. Mas nós não compartimos da sua refeição; nós não
realizamos uma permuta. Isso ainda lhe pertence, brâmane! Isso ainda lhe
pertence, brâmane!”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O rei e a
sua corte entendem que o contemplativo Gotama é um arahant, no entanto o Mestre
Gotama ainda fica enraivecido.” [<a href="#N1">1</a>]<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><a name=R1></a><span lang=PT-BR
style='mso-ansi-language:PT-BR'>[O Abençoado:]<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Como pode a raiva surgir naquele que eliminou a raiva, <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>naquele que é domado e tem um modo de vida virtuoso,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>naquele que é libertado através do perfeito conhecimento,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>naquele que é firme e que permanece em paz?<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Quem retribui a um homem enraivecido com a raiva <br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>desse modo faz com que as coisas piorem para ele mesmo.<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>Não repagando a raiva com a raiva,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>ele sai vitorioso numa batalha difícil de ser vencida.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Ele pratica para o bem estar de ambos -<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>dele mesmo e dos outros -<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>quando, sabendo que o seu oponente está enraivecido,<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>ele com atenção plena se mantém em paz.<o:p></o:p></span></p>


<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Alcançando a cura para ambos -<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>dele mesmo e dos outros -<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>as pessoas que o consideram um tolo<br>

<span lang=PT-BR style='mso-ansi-language:
PT-BR'>desconhecem o<span style='mso-spacerun:yes'>  </span>Dhamma.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isso
foi dito, o brâmane Akkosaka Bharadvaja disse para o Abençoado:<span
style='mso-spacerun:yes'>  </span>“Magnífico, Mestre Gotama! Magnífico, Mestre
Gotama! Mestre Gotama esclareceu o Dhamma de várias formas, como se tivesse
colocado em pé o que estava de cabeça para baixo, revelasse o que estava
escondido, mostrasse o caminho para alguém que estivesse perdido ou segurasse
uma lâmpada no escuro para aqueles que possuem visão pudessem ver as formas. Nós
buscamos refúgio no Mestre Gotama, no Dhamma e na Sangha dos bhikkhus. Que eu
possa receber a admissão na vida santa sob o Mestre Gotama, que eu possa
receber a admissão completa”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, o
brâmane do clã Bharadvaja recebeu a admissão na vida santa sob o Mestre Gotama,
e ele recebeu a admissão completa. Permanecendo só, isolado, diligente, ardente
e decidido, em pouco tempo, ele alcançou e permaneceu no objetivo supremo da
vida santa pelo qual membros de um clã deixam a vida em família pela vida
santa, tendo conhecido e realizado por si mesmo no aqui e agora, ele soube: “O
nascimento foi destruído, a vida santa foi vivida, o que deveria ser feito foi
feito, não há mais vir a ser a nenhum estado.” E assim o Venerável Bharadvaja
tornou-se mais um dos Arahants.<o:p></o:p></span></p>



