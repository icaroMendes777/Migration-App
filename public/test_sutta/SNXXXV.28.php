<p class=Normal> Em certa ocasião, o Abençoado estava em Gaya, em Gayasissa, com 1.000
bhikkhus. Lá ele se dirigiu aos bhikkhus: <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR>&quot;Bhikkhus, o todo está em chamas. E qual é esse todo
que está em chamas? O olho está em chamas, as formas estão em chamas, a
consciência no olho está em chamas, o contato no olho está em chamas, e
qualquer sensação que surja tendo o contato no olho como condição – quer seja
prazerosa, dolorosa ou nem prazerosa, nem dolorosa - isso também está em
chamas. Em chamas com o que? Em chamas com o fogo da cobiça, o fogo da aversão,
o fogo da delusão. Em chamas, eu lhes digo, com o nascimento, envelhecimento e
morte, com a tristeza, lamentação, dor, angústia e desespero. </span></p>

<p class=Normal><span lang=PT-BR>&quot;O ouvido está em chamas, os sons estão em chamas...</span></p>

<p class=Normal><span lang=PT-BR>&quot;O nariz está em chamas, os aromas estão em chamas...</span></p>

<p class=Normal><span lang=PT-BR>&quot;A língua está em chamas, os sabores estão em
chamas...</span></p>

<p class=Normal><span lang=PT-BR>&quot;O corpo está em chamas, os tangíveis estão em
chamas...</span></p>

<p class=Normal><span lang=PT-BR>&quot;A mente está em chamas, os objetos mentais estão em
chamas, a consciência na mente está em chamas, o contato na mente está em
chamas, e qualquer sensação que surja tendo o contato na mente como condição -
quer seja prazerosa, dolorosa ou nem prazerosa, nem dolorosa. Em chamas com o
que? Em chamas com o fogo da cobiça, o fogo da aversão, o fogo da delusão. Em
chamas, eu lhes digo, com o nascimento, envelhecimento e morte, com a tristeza,
lamentação, dor, angústia e desespero. </span></p>

<p class=Normal><span lang=PT-BR>&quot;Vendo desse modo, o nobre discípulo se desencanta com
o olho, desencanta com as formas, desencanta com a consciência no olho,
desencanta com o contato no olho, desencanta com qualquer sensação que surja na
dependência do contato no olho – quer seja prazerosa, dolorosa ou nem
prazerosa, nem dolorosa. </span></p>

<p class=Normal><span lang=PT-BR>&quot;Ele se desencanta com o ouvido... </span></p>

<p class=Normal><span lang=PT-BR>&quot;Ele se desencanta com o nariz... </span></p>

<p class=Normal><span lang=PT-BR>&quot;Ele se desencanta com a língua... </span></p>

<p class=Normal><span lang=PT-BR>&quot;Ele se desencanta com o corpo... </span></p>

<p class=Normal><span lang=PT-BR>&quot;Ele se desencanta com a mente, desencanta com os objetos
mentais, desencanta com a consciência na mente, desencanta com o contato na
mente, desencanta com qualquer sensação que surja tendo o contato na mente como
condição – quer seja prazerosa, dolorosa ou nem prazerosa, nem dolorosa.
Desencantado ele se torna desapegado. Através do desapego a sua mente é
libertada. Quando ela está libertada surge o conhecimento: ‘Libertada.’ Ele
compreende que: ‘O nascimento foi destruído, a vida santa foi vivida, o que
deveria ser feito foi feito, não há mais vir a ser a nenhum estado.’”</span></p>

<p class=Normal><span lang=PT-BR>Isso foi o que o Abençoado disse. Os bhikkhus ficaram
satisfeitos e contentes com as palavras do Abençoado. E enquanto o discurso
estava sendo proferido, os 1.000 bhikkhus, através do desapego, foram
completamente libertados das impurezas.</span></p>


