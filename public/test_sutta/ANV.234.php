<p class=Normal> "Bhikkhus, possuindo cinco qualidades, um bhikkhu residente é de grande ajuda num monastério. Quais cinco? (1) Aqui, um bhikkhu é virtuoso; ele permanece contido pelas regras do Patimokkha, perfeito na conduta e na sua esfera de atividades. Temendo a menor falha, ele treina adotando os preceitos de virtude. (2) Ele aprendeu muito, se recorda daquilo que aprendeu e consolida aquilo que aprendeu. Aqueles ensinamentos que são admiráveis no início, admiráveis no meio, admiráveis no final, com o correto significado e fraseado e que revelam uma vida santa que é completamente perfeita e imaculada – ensinamentos como esses ele os aprendeu bem, se recorda, domina com a linguagem, investigou com a mente e penetrou corretamente com o entendimento. (3) Ele conserta aquilo que está quebrado e partido. (4) Quando uma grande sangha de bhikkhus chega ao monastério incluindo bhikkhus de várias regiões ele se dirige às pessoas leigas e as informa: 'Amigos, uma grande sangha de bhikkhus chegou ao monastério incluindo bhikkhus de várias regiões. Façam mérito. Essa é uma ocasião para fazer mérito.' (5) Ele obtém de acordo com a sua vontade, sem problemas ou dificuldades, os quatro jhanas que constituem a mente superior e que proporcionam uma estada prazerosa aqui e agora. Possuindo essas cinco qualidades, um bhikkhu residente é de grande ajuda num monastério."</p>
