<p class=Normal> Em certa ocasião o Abençoado estava em Rajagaha, no Bambual, no Santuário dos Esquilos. Então o venerável Samiddhi foi até o Abençoado e depois de cumprimentá-lo sentou a um lado e disse:</p>

<p class=Normal> "Venerável senhor, é dito, 'um ser, um ser.' De que modo venerável senhor pode haver um ser ou a designação um ser?"</p>

<p class=Normal>"Onde há o olho, Samiddhi, onde há formas, consciência no olho, coisas para serem conscientizadas pela consciência no olho, nisso existe um ser ou a designação um ser.</p>

<p class=Normal>"Onde há o ouvido ... a mente, onde há objetos mentais, consciência na mente, coisas para serem conscientizadas pela consciência na mente, nisso existe um ser ou a designação um ser.</p>

<p class=Normal>"Onde não há o olho, Samiddhi, onde não há formas, consciência no olho, não há coisas para serem conscientizadas pela consciência no olho, nisso não existe um ser nem a designação um ser.</p>

<p class=Normal>"Onde não há o ouvido ... não há a mente, onde não há objetos mentais, consciência na mente, não há coisas para serem conscientizadas pela consciência na mente, nisso não existe um ser nem a designação um ser."</p>

