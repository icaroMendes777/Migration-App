<p class=Normal> Em Savatthi. "Bhikkhus eu busquei a gratificação na forma. Qualquer gratificação que possa haver na forma - isso eu descobri. Eu vi claramente através da sabedoria até onde se estende a gratificação na forma. </p>

<p class=Normal>"Bhikkhus, eu busquei o perigo na forma. Qualquer perigo que possa haver na forma - isso eu descobri. Eu vi claramente através da sabedoria até onde se estende o perigo na forma. </p>

<p class=Normal>"Bhikkhus, eu busquei a escapatória da forma. Qualquer escapatória da forma que possa haver - isso eu descobri. Eu vi claramente através da sabedoria até onde se estende a escapatória da forma. </p>

<p class=Normal>"Bhikkhus eu busquei a gratificação ... o perigo ... a escapatória na sensação ... na percepção ... nas formações volitivas ... na consciência. Qualquer escapatória que possa haver na consciência - isso eu descobri. Eu vi claramente através da sabedoria até onde se estende a escapatória da consciência. </p>

<p class=Normal> “Bhikkhus, enquanto não obtive o conhecimento direto de como na verdade são a gratificação, o perigo, e a escapatória desses cinco agregados influenciados pelo apego, não reivindiquei ter despertado para a insuperável perfeita iluminação neste mundo com os seus devas, maras e brahmas, esta população com seus contemplativos e brâmanes, seus príncipes e povo. Mas quando obtive o conhecimento direto de como na verdade são a gratificação, o perigo, e a escapatória desses cinco agregados influenciados pelo apego, reivindiquei ter despertado para a insuperável perfeita iluminação neste mundo com os seus devas, maras e brahmas, esta população com seus contemplativos e brâmanes, seus príncipes e povo. O conhecimento e visão surgiram em mim: ‘A libertação da minha mente é inabalável. Este é o meu último nascimento. Não há mais vir a ser a nenhum estado.’" </p>


