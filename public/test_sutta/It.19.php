<p class=Normal> Isto foi dito pelo Abençoado, dito pelo Arahant, assim ouvi: "Há uma coisa, bhikkhus, que quando surge no mundo, surge em benefício de muitas pessoas, causando felicidade em muitas pessoas, causando o ganho, o bem e a felicidade em devas e humanos. Qual é essa coisa? É a união na sangha. Quando a sangha está unida não há brigas, recriminações, difamações, e expulsões. Nessa situação aqueles que não são devotos são convertidos e aumenta a fé daqueles que são devotos."</p>

<p class=MsoNormal style='margin-left:.75in'> A harmonia da sangha é prazerosa.<br>
