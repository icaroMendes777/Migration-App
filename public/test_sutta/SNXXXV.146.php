<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
eu explicarei para vocês o kamma passado, o kamma novo, a cessação de kamma e o
caminho para a cessação de kamma. Ouçam e prestem muita atenção àquilo que eu
vou dizer.” – “Sim, venerável senhor,” os bhikkhus responderam. O Abençoado
disse o seguinte: <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O que, bhikkhus, é kamma
passado? Olho ... ouvido ... nariz ... língua ... corpo ... mente devem ser
compreendidos como kamma passado, formados por condições, nascidos das
volições, a base para as sensações. Isso é chamado de ‘kamma passado.’<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus, o que é ‘kamma
novo’? Ações com o corpo, linguagem e mente no momento presente, isso é chamado
de ‘kamma novo.’ <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus, o que é a
cessação de kamma? Quando alguém alcança a libertação através da cessação das
ações com o corpo, linguagem e mente, isso é chamado de ‘cessação de kamma.’ <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus, e qual é o
caminho para a cessação de kamma? É este Nobre Caminho Óctuplo, isto é,
Entendimento Correto .... Concentração Correta. Isso é chamado ‘o caminho que
conduz à cessação de kamma.’” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto, bhikkhus, eu
ensinei para vocês o kamma passado e o novo, a cessação de kamma e o caminho
para a cessação de kamma. Aquilo que por compaixão um Mestre deveria fazer para
os seus discípulos, desejando o bem-estar deles, isso eu fiz por vocês. Ali
estão aquelas árvores, aquelas cabanas vazias. Meditem, bhikkhus, não
adiem, ou então vocês se arrependerão mais tarde. Essa é a nossa instrução para
vocês.”<span style="mso-spacerun: yes">  </span><o:p></o:p></span></p>


