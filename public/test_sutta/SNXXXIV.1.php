<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, há quatro tipos de meditadores. Quais quatro?<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui,
bhikkhus, um meditador tem habilidade na concentração com relação à
concentração, mas não tem habilidade na realização da concentração.
[<a href="#N1">1</a>]<o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Aqui, um meditador tem habilidade na realização da
concentração, mas não tem habilidade na concentração com relação à
concentração.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui, um
meditador não tem habilidade na concentração com relação à concentração e
tampouco tem habilidade na realização da concentração.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Aqui, um
meditador tem habilidade, tanto na concentração com relação à concentração como
na realização da concentração.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Com
respeito a isso, bhikkhus, o meditador que tem<span style="mso-spacerun: yes"> 
</span>habilidade, tanto na concentração com relação à concentração como na realização
da concentração, é o cabeça, o melhor, o principal, o supremo, o
mais excelente de todos esses quatro tipos de meditadores. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Da mesma
forma, bhikkhus, como da vaca se obtém o leite, do leite o creme de leite, do
creme de leite a manteiga, da manteiga a manteiga líquida e da manteiga líquida
o creme da manteiga líquida, que é considerado o melhor dentre todos esses,
assim também, o meditador que tem<span style="mso-spacerun: yes"> 
</span>habilidade, tanto na concentração com relação à concentração como na
realização da concentração, é o cabeça, o melhor, o principal, o
supremo, o mais excelente de todos esses quatro tipos de meditadores.”<o:p></o:p></span></p>




