<p class="Normal"><span lang=PT-BR mso-ansi-language:PT-BR'>O Udana, o terceiro livro do  Khuddaka Nikaya que consiste de oitenta suttas curtos, recebe esse nome do pronunciamento feito ao final de cada discurso, em geral sob a forma de versos, prefaciado pela frase “Então, dando-se conta do significado disso, o Abençoado nessa ocasião exclamou”. Os oitenta suttas estão organizados em oito vaggas, ou capítulos.</span></p>

