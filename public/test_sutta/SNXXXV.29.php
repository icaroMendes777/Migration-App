<p class=Normal> Assim ouvi. Em certa ocasião o Abençoado estava em Rajagaha, no Bambual, no Santuário dos Esquilos. Então o Abençoado se dirigiu aos bhikkhus:</p>

<p class=Normal>"Bhikkhus, o todo está oprimido. Qual, bhikkhus, é o todo  que está oprimido? O olho está oprimido, as formas estão oprimidas, a consciência no olho está oprimida, o contato no olho está oprimido, e qualquer sensação que surja tendo o contato no olho como condição – quer seja prazerosa, dolorosa ou nem prazerosa, nem dolorosa - isso também está oprimido. Oprimido pelo quê? Oprimido pelo nascimento, envelhecimento, e morte; pela tristeza, lamentação, dor, angústia e desespero, eu digo.</p>

<p class=Normal>"O ouvido está oprimido ... a mente está oprimida ... Oprimida pelo quê? Oprimida pelo nascimento, envelhecimento, e morte; pela tristeza, lamentação, dor, angústia e desespero, eu digo.</p>

<p class=Normal>"Vendo assim, bhikkhus, o nobre discípulo bem instruído experimenta o desencantamento em relação ao olho, o desencantamento em relação ao nariz, o desencantamento em relação à língua, o desencantamento em relação ao corpo, o desencantamento em relação à mente. Experimentando o desencantamento ele se torna desapegado. Através do desapego a sua  mente está libertada. Quando está libertada, surge o conhecimento, ‘Libertada.’ Ele compreende que ‘O nascimento foi destruído, a vida santa foi vivida, o que devia ser feito foi feito, não há mais vir a ser a nenhum estado.'"</p>

