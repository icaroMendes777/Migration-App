<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. “Bhikkhus, eu ensinarei para vocês a agitação devido ao apego e a não
agitação devido ao não apego. [<a href="#N1">1</a>] <a name=R1></a>Ouçam e
prestem muita atenção àquilo que eu vou dizer.” – “Sim, venerável senhor,” os
bhikkhus responderam. O Abençoado disse o seguinte:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Porque,
amigos, existe agitação devido ao apego? Neste caso, uma pessoa comum sem instrução
que não respeita os nobres, que não é proficiente nem treinada no Dhamma
deles, que não respeita os homens verdadeiros, que não é proficiente nem
treinada no Dhamma deles, considera a forma material como sendo o eu, ou o
eu possuído de forma material, ou a forma material como estando no eu, ou o eu
como estando na forma material. Essa forma material muda e se altera. Com a
forma material mudando e se alterando, a sua consciência se preocupa com essa
mudança. Estados mentais agitados nascidos da preocupação com a mudança da
forma material surgem ao mesmo tempo e permanecem obcecando a mente. Como a
mente dele está obcecada, ele fica ansioso, angustiado e preocupado e devido ao
apego ele se torna agitado. <o:p></o:p></span></p>


<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele
considera a sensação como sendo o eu … Ele considera a percepção como sendo o
eu ... Ele considera as formações volitivas como sendo o eu … Ele considera a consciência
como sendo o eu, ou o eu possuído de consciência, ou a consciência como estando
no eu, ou o eu como estando na consciência. Essa consciência muda e se altera.
Com a consciência mudando e se alterando, a sua consciência se preocupa com
essa mudança. Estados mentais agitados nascidos da preocupação com a mudança da
consciência surgem ao mesmo tempo e permanecem obcecando a mente. Como a mente
dele está obcecada, ele fica ansioso, angustiado e preocupado e devido ao apego
ele se torna agitado. Assim é como existe agitação devido ao apego.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E porque,
amigos, existe a não agitação devido ao não apego? Neste caso um nobre
discípulo bem instruído, que respeita os nobres, que é proficiente e
treinado no Dhamma deles, que respeita os homens verdadeiros, que é
proficiente e treinado no Dhamma deles, não considera a forma material como
sendo o eu, ou o eu possuído de forma material, ou a forma material como
estando no eu, ou o eu como estando na forma material. Essa forma material muda
e se altera. Com a forma material mudando e se alterando, a sua consciência não
se preocupa com essa mudança. Estados mentais agitados nascidos da preocupação
com a mudança da forma material não surgem ao mesmo tempo e não permanecem
obcecando a mente. Como a mente dele não está obcecada, ele não fica ansioso,
angustiado e preocupado e devido ao não apego ele não se torna agitado.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele não
considera a sensação como sendo o eu….Ele não considera a percepção como sendo
o eu….Ele não considera as formações volitivas como sendo o eu….Ele não considera a
consciência como sendo o eu, ou o eu possuído de consciência, ou a consciência
como estando no eu, ou o eu como estando na consciência. Essa consciência muda
e se altera. Com a consciência mudando e se alterando, a sua consciência não se
preocupa com essa mudança. Estados mentais agitados nascidos da preocupação com
a mudança da consciência não surgem ao mesmo tempo e não permanecem obcecando a
mente. Como a mente dele não está obcecada, ele não fica ansioso, angustiado e
preocupado e devido ao não apego ele não se torna agitado. Assim é como existe
a não agitação devido ao não apego.”<o:p></o:p></span></p>

