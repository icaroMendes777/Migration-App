<p class=Normal>Então, ao amanhecer, vários bhikkhus se vestiram e tomando as suas tigelas e os mantos externos foram para Savatthi para esmolar alimentos. Então, eles pensaram: “Ainda é muito cedo para esmolar alimentos em Savatthi. E se nós fossemos até o parque dos errantes de outras seitas,” e assim eles foram até o parque dos errantes de outras seitas e ao chegar os cumprimentaram. Quando a conversa amigável e cortês havia terminado, eles sentaram a um lado. Os errantes lhes disseram: </p>
<p class=Normal> “Amigos, o contemplativo Gotama ensina o Dhamma aos seus discípulos deste modo: ‘Venham, bhikkhus, abandonem os cinco obstáculos, as corrupções da mente que enfraquecem a sabedoria e desenvolvam corretamente os sete fatores da iluminação.’ Nós também ensinamos aos nossos discípulos deste modo: ‘Venham, amigos, abandonem os cinco obstáculos, as corrupções da mente que enfraquecem a sabedoria, e desenvolvam corretamente os sete fatores da iluminação.’ </p> 
<p class=Normal>Qual é então a distinção, amigos, qual é a variação, qual é a diferença entre o ensinamento do Dhamma do contemplativo Gotama e o nosso, entre as instruções dele e as nossas?” [<a href="#N1">1</a>] </p>
<p class=Normal><a name="R1"> </a>Então, aqueles bhikkhus nem aprovaram nem desaprovaram as palavras dos errantes. Sem dizer isto ou aquilo eles se levantaram dos seus assentos e foram embora, pensando: “Devemos entender o significado dessas palavras na presença do Abençoado.” </p>
<p class=Normal>Depois de terem esmolado alimentos em Savatthi e retornado, após a refeição, eles foram até o Abençoado e depois de cumprimentá-lo sentaram a um lado e relataram o que havia ocorrido. [O Abençoado disse:] </p>
<p class=Normal> “Bhikkhus, os errantes de outras seitas que assim falam devem ser questionados da seguinte forma: ‘Amigos, há um modo de exposição através do qual os cinco obstáculos se convertem em dez e os sete fatores da iluminação se convertem em catorze?’ Sendo questionados dessa forma, os errantes de outras seitas fracassarão na exposição desse assunto, e mais ainda, eles se meterão em dificuldades. Por que isso? Porque não é o território deles. Bhikkhus, eu não vejo ninguém no mundo com os seus devas, Maras e Brahmas, esta população com os seus contemplativos e brâmanes, seus príncipes e povo, que pudesse satisfazer a mente com uma resposta a essas questões, exceto o Tathagata ou os seus discípulos, ou alguém que tenha aprendido com eles. </p>
<p class=Normal> <b>(I. Cinco se convertem em dez)</b></p>

<p class=Normal> “E qual, bhikkhus, é o modo de exposição através do qual os cinco obstáculos se convertem em dez? </p>

<p class=Normal> “Qualquer desejo sensual pelo interno é um obstáculo; qualquer desejo sensual pelo externo também é um obstáculo. [<a href="#N2">2</a>]<a name="R2"> </a>Assim, aquilo que é dito de forma concisa como o obstáculo do desejo sensual, através desse modo de exposição se converte em dois. </p>

<p class=Normal> “Qualquer má vontade pelo interno é um obstáculo; qualquer má vontade  pelo externo também é um obstáculo. Assim, aquilo que é dito de forma concisa como o obstáculo da má vontade, através desse modo de exposição se converte em dois. </p>

<p class=Normal> “Qualquer preguiça é um obstáculo; qualquer torpor também é um obstáculo. Assim, aquilo que é dito de forma concisa como o obstáculo da preguiça e torpor, através desse modo de exposição se converte em dois. </p>

<p class=Normal> “Qualquer inquietação é um obstáculo; qualquer ansiedade também é um obstáculo. Assim, aquilo que é dito de forma concisa como o obstáculo da inquietação e ansiedade, através desse modo de exposição se converte em dois. </p>

<p class=Normal> “Qualquer dúvida pelo interno é um obstáculo; qualquer dúvida  pelo externo também é um obstáculo. Assim, aquilo que é dito de forma concisa como o obstáculo da dúvida, através desse modo de exposição se converte em dois. </p>

<p class=Normal> “Esse, bhikkhus, é o modo de exposição através do qual os cinco obstáculos se convertem em dez.” </p>
 

<p class=Normal> <b>(II. Sete se convertem em catorze)</b>

<p class=Normal> “E qual, bhikkhus, é o modo de exposição através do qual os sete fatores da iluminação se convertem em catorze? </p>

<p class=Normal> “Qualquer atenção plena em relação ao que é interno é o fator da iluminação da atenção plena; qualquer atenção plena em relação ao que é externo também é o fator da iluminação da atenção plena. Assim, aquilo que é dito de forma concisa como o fator da iluminação da atenção plena, através desse modo de exposição se converte em dois. </p>

<p class=Normal> “Sempre que alguém discerne as coisas internamente com sabedoria, as examina, faz uma investigação delas, esse é o fator da iluminação da investigação dos fenômenos; sempre que alguém discerne as coisas externamente com sabedoria, as examina, faz uma investigação delas, esse é o fator da iluminação da investigação dos fenômenos. Assim, aquilo que é dito de forma concisa como o fator da iluminação da investigação dos fenômenos, através desse modo de exposição se converte em dois. [<a href="#N3">3</a>] </p>

<p class=Normal><a name="R3"> </a> “Qualquer energia corporal é o fator da iluminação da energia; qualquer energia mental também é o fator da iluminação da energia.  Assim, aquilo que é dito de forma concisa como o fator da iluminação da energia, através desse modo de exposição se converte em dois. </p>

<p class=Normal> “Qualquer êxtase acompanhado pelo pensamento aplicado e pelo pensamento sustentado é o fator da iluminação do êxtase; qualquer êxtase sem o pensamento aplicado e sem o pensamento sustentado também é o fator da iluminação do êxtase. [<a href="#N4">4</a>] <a name="R4"> </a>Assim, aquilo que é dito de forma concisa como o fator da iluminação da energia, através desse modo de exposição se converte em dois. </p>

<p class=Normal> “Qualquer calma corporal é o fator da iluminação da tranqüilidade; qualquer tranqüilidade mental também é o fator da iluminação da tranqüilidade.  Assim, aquilo que é dito de forma concisa como o fator da iluminação da tranqüilidade, através desse modo de exposição se converte em dois. [<a href="#N5">5</a>]</p>

<p class=Normal><a name="R5"> </a> “Qualquer concentração acompanhada pelo pensamento aplicado e pelo pensamento sustentado é o fator da iluminação da concentração; qualquer concentração sem o pensamento aplicado e sem o pensamento sustentado também é o fator da iluminação da concentração. [<a href="#N6">6</a>] <a name="R6"> </a>Assim, aquilo que é dito de forma concisa como o fator da iluminação da concentração, através desse modo de exposição se converte em dois. </p>

<p class=Normal> “Qualquer equanimidade em relação ao que é interno é o fator da iluminação da equanimidade; qualquer equanimidade em relação ao que é externo também é o fator da iluminação da equanimidade. Assim, aquilo que é dito de forma concisa como o fator da iluminação da equanimidade, através desse modo de exposição se converte em dois. </p>

<p class=Normal> “Esse, bhikkhus, é o modo de exposição através do qual os sete fatores da iluminação se convertem em catorze.” </p>

