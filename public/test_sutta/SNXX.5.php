<p class=Normal>Em Savatthi. &quot;Bhikkhus, suponham que houvesse uma lança com a
lâmina bem afiada e um homem chegasse pensando, 'Com a mão ou o punho eu
entortarei essa lâmina afiada da lança, irei dobrá-la em dois e enrolá-la.' <o:p></o:p></span></p>


<p class=Normal>&quot; O que vocês pensam? Com a mão ou o punho seria esse homem capaz de<span
style="mso-spacerun: yes">  </span>entortar essa lâmina afiada da lança,
dobrá-la e<span style="mso-spacerun: yes">  </span>enrolá-la?<o:p></o:p></span></p>


<p class=Normal> &quot;Não, senhor. Por que isso? Porque uma lâmina afiada de uma lança
não é fácil de ser entortada, dobrada, ou enrolada. O homem simplesmente
experimentaria irritação e cansaço.&quot; <o:p></o:p></span></p>


<p class=Normal> &quot;Da mesma forma, bhikkhus, quando um bhikkhu cultivou e
desenvolveu a libertação da mente através do amor bondade, fez dela o seu
veículo,<span style="mso-spacerun: yes">  </span>a sua base,<span
style="mso-spacerun: yes">  </span>estabilizou-a, se exercitou nela e
aperfeiçoou- a, se algum ser não humano quiser controlar a mente dele,<span
style="mso-spacerun: yes">  </span>experimentaria só irritação e cansaço.” <o:p></o:p></span></p>


<p class=Normal> “Portanto, bhikkhus, assim vocês deveriam praticar:<span
style="mso-spacerun: yes">  </span>‘Nós cultivaremos e desenvolveremos a
libertação da mente através do amor bondade, faremos dela o nosso veículo, a
nossa base, estabilizá-la-emos, nela nos exercitaremos e aperfeiçoá-la-emos.’ Assim
é como vocês devem praticar.” <o:p></o:p></span></p>


