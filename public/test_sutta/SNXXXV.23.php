<p class=Normal><a name=n1></a><span lang=PT-BR style='mso-ansi-language:PT-BR'>[1] De
acordo com o comentário, o todo, <i>(sabba,) </i>é de quatro tipos: (a) o todo
completo, <i>(sabbasabba), </i>isto é, tudo aquilo que pode ser conhecido, tudo
que compõe o âmbito da onisciência do Buda. (b) o todo das bases dos sentidos, <i>(ayatanasabba),
</i>isto é, os fenômenos dos quatro planos; (c) o todo da individualidade
pessoal, <i>(sakkayasabba), </i>isto é, os fenômenos dos três planos mundanos;
e (d) o todo parcial, <i>(padesabba), </i>isto é, os cinco objetos dos sentidos
físicos. Cada um destes, de (a) até (d), tem um âmbito mais restrito que o
anterior. Neste sutta a referência é feita a (b) o todo das bases dos sentidos. Os
quatro planos correspondem aos três planos mundanos (reino da esfera sensual,
reino da matéria sutil e reino imaterial) mais o plano supramundano (os quatro
caminhos, os seus frutos e Nibbana).[<a href="#R1">Retorna</a>] <o:p></o:p></span></p>

<p class=Normal><b><span lang=PT-BR style='mso-ansi-language:PT-BR'>Comentário de Thanissaro
Bhikkhu<o:p></o:p></span></b></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>O tratamento dado a este
discurso pelo Comentário é bastante singular. Para começar, ele delineia outros
três “Todo” em adição àquele definido neste discurso, um deles supostamente
mais abrangente do que aquele aqui definido:<span style='mso-spacerun:yes'> 
</span>o todo completo que constitui o âmbito da onisciência do Buda, (que tudo
sabe, no sentido literal). Isso, apesar do discurso dizer que a descrição desse
todo está além do alcance das explicações. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Um segundo ponto, o
Comentário inclui nibbana, (libertação), dentro do alcance do Todo aqui
descrito – como um dhamma, ou objeto da mente – embora existam muitos outros
discursos no Cânone mencionando de maneira inequívoca que nibbana se encontra
além do alcance dos seis sentidos e seus objetos. O <a href="SnpV6.php">Snp V.6</a>,
por exemplo, indica que uma pessoa que tenha alcançado nibbana superou todos os
fenômenos, <i>(sabbe dhamma),</i> e portanto não pode ser descrita. O <a
href="MN49.php#T25">MN 49.25</a> aborda a “consciência desprovida de
atributos”,<i> (viññanam anidassanam), </i><span style='mso-bidi-font-style:
italic'>que não faz parte da “Totalidade do Todo.” Além disso o </span><a
href="SNXXXV.24.php">SN.XXXV.24</a> diz que o “Todo” deve ser abandonado. Em
nenhuma instância no Cânone é mencionado que nibbana deva ser abandonado.
Nibbana vem em seguida à cessação, <i>(nirodha), </i><span style='mso-bidi-font-style:
italic'>que deve ser realizado. Uma vez<span style='mso-spacerun:yes'> 
</span>que Ni</span>bbana seja realizado, não há nenhuma outra tarefa a ser
realizada. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Portanto, parece que a
abordagem deste discurso sobre o “Todo” tem como propósito limitar o uso da
palavra “todo” em todos os ensinamentos do Buda às seis bases internas e
externas. O <a href="SNXXXV.24.php">SN.XXXV.24</a> mostra que ele também inclui
a consciência, contato e as sensações conectadas com as bases dos sentidos e os
seus objetos. Nibbana estaria fora dessa palavra, “todo.” Isso também se
encaixa com outro ponto que aparece várias vezes no Cânone: que o desapego é o
dhamma mais elevado (<a name=PVW></a><a href="It.90.php"><span
style='mso-bookmark:PVW'>It</span> 90</a>), enquanto que o arahant superou até
mesmo o desapego (<a href="SnpIV.6.php">Snp IV.6</a>; <a href="SnpIV.10.php">Snp
IV.10</a>). <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso levanta uma questão,
se a palavra “todo” não inclui nibbana, isso significa que poderíamos inferir
da afirmativa, “todos os fenômenos são não-eu”, que nibbana é o eu? A resposta
é não. Como mencionado no AN IV.173, até mesmo a pergunta sobre se algo
permanece ou não permanece (ou ambos, ou nenhum dos dois) depois da cessação das
seis bases dos sentidos é diferenciar aquilo que por natureza não é diferenciado
(ou complicar o não complicado – veja o <a href="MN18.php">MN 18</a>). A
abrangência da dualidade se estende apenas ao “Todo.” As percepções do eu ou
não-eu, que constituem a dualidade, não se aplicam para além do “Todo.” Quando
a cessação do “Todo” é experimentada, toda dualidade é apaziguada. <o:p></o:p></span></p>






