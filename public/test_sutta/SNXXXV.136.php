<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
devas e humanos sentem prazer com as formas, se deliciam com as formas, se
alegram com as formas. Com a mudança, desaparecimento e cessação das formas,
devas e humanos permanecem sofrendo. Devas e humanos sentem prazer com os sons
... prazer com os aromas ... prazer com os sabores ... prazer com os tangíveis
... prazer com os objetos mentais, se deliciam com os objetos mentais, se
alegram com os objetos mentais. Com a mudança, desaparecimento e cessação dos
objetos mentais, devas e humanos permanecem sofrendo.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Mas,
bhikkhus, o Tathagata, um arahant, perfeitamente iluminado, compreendeu
como na verdade são a origem e a cessação, a gratificação, o perigo e a
escapatória no caso das formas. Ele não sente prazer nas formas, ele não se
delicia com as formas, ele não se alegra com as formas. Com a mudança,
desaparecimento e cessação das formas, o Tathagata permanece feliz.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Ele
compreendeu como na verdade são a origem e a cessação, a gratificação, o perigo
e a escapatória no caso dos sons ... aromas ... sabores ... tangíveis ...
objetos mentais. Ele não sente prazer nos objetos mentais, ele não se delicia
com os objetos mentais, ele não se alegra com os objetos mentais. Com a
mudança, desaparecimento e cessação dos objetos mentais, o Tathagata permanece
feliz.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o
que o Abençoado disse. Tendo disto isso, o Mestre, disse mais:<o:p></o:p></span></p>



