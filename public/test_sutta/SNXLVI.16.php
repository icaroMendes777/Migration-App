<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Certa
ocasião, o Abençoado estava em Rajagaha, no Bambual, no Santuário dos Esquilos.
Agora, naquela ocasião, o Abençoado estava doente, aflito, gravemente enfermo.
Então o Venerável Maha Cunda [o irmão mais jovem do <a name=PVW>Ven</a>.
Sariputta] foi até o Abençoado e depois de cumprimentá-lo sentou a um lado. Depois
dele ter sentado o Abençoado disse:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Recite os
fatores da iluminação, Cunda.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Esses sete
fatores da iluminação, Venerável Senhor, foram bem expostos pelo Abençoado;
quando desenvolvidos e cultivados, eles conduzem ao conhecimento direto, à
iluminação, a Nibbana. Quais sete? O fator da iluminação da atenção plena foi
bem exposto pelo Abençoado; quando desenvolvido e cultivado, ele conduz ao
conhecimento direto, à iluminação, a Nibbana ... o fator da iluminação da
equanimidade foi bem exposto pelo Abençoado; quando desenvolvido e cultivado,
ele conduz ao conhecimento direto, à iluminação, a Nibbana. Esses sete fatores
da iluminação foram bem expostos pelo Abençoado; quando desenvolvidos e
cultivados, eles conduzem ao conhecimento direto, à iluminação, a Nibbana.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Com
certeza, Cunda, esses são fatores da iluminação! Com certeza, Cunda, esses são
fatores da iluminação!”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o
que disse o Venerável Maha Cunda. O Mestre aprovou. E o Abençoado se recuperou
daquela enfermidade. Dessa maneira o Abençoado foi curado da sua enfermidade. <o:p></o:p></span></p>

