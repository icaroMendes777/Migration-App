contato ... O nariz como base para o
contato ... A língua como base para o
contato ... O corpo como base para o
contato ... A mente como base para o contato – se estiver destreinada, desguardada,
desprotegida, descontrolada – traz o sofrimento. Essas seis
bases para o contato<span style="mso-spacerun: yes">  </span>– se estiverem
destreinadas, desguardadas, desprotegidas, descontroladas – trazem o sofrimento.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus
essas seis bases para o contato – se estiverem treinadas, bem guardadas, bem
protegidas, bem controladas – trazem a felicidade. [<a href="#N1">1</a>] <a
name=R1></a>Quais seis?<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“O olho,
bhikkhus, como base para o contato – se estiver treinado, bem guardado, bem
protegido, bem controlado – traz a felicidade. O ouvido como base para o
contato ... O nariz como base para o
contato ... A língua como base para o
contato ... O corpo como base para o
contato ... A mente como base para o contato se estiver treinada, bem guardada, bem
protegida, bem controlada – traz a felicidade. Essas seis
bases para o contato – se estiverem treinadas, bem guardadas, bem protegidas,
bem controladas – trazem a felicidade.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o
que o Abençoado disse. Tendo dito isso, o Iluminado, o Mestre, disse ainda
mais:<o:p></o:p></span></p>



<p class=Normal style='margin-left:.75in'><span lang=PT-BR style='mso-ansi-language:
