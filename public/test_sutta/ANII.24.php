<p class=Normal><b>Notas:</b></p>

<p class=Normal><a name=N1></a> [1]  A questão sobre quais discursos do Buda são explícitos e quais exigem interpretação acabou se tornando um dos tópicos mais intensamente debatidos na hermenêutica Budista. Desde as primeiras escolas na Índia, o debate teve seguimento em textos mais recentes como nos sutras Mahayana Aksayamatinirdesa e Samdhinirmocana. A controvérsia continuou mesmo fora da índia, no Sri Lanka, na China e no Tibet. Nos comentários em pali a controvérsia foi decidida com base na distinção feita no Abhidhamma entre a realidade convencional e a realidade última: "Aqueles suttas que falam de uma pessoa, duas pessoas, etc., exigem interpretação pois o seu significado deve ser interpretado à luz de que num sentido último uma pessoa não existe. Um sutta explícito é aquele que trata da impermanência, sofrimento e não-eu pois nesse caso o significado é simplesmente a impermanência, sofrimento e não-eu." [<a href="#R1">Retorna</a>]</p>

