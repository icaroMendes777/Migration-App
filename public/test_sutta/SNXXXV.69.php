<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Certa<a
name=sariputta> vez, o Venerável Sariputta</a> e o <a name=upasena>Venerável Upasena</a>
estavam em <a name=rajagaha>Rajagaha</a> no Bosque Fresco, na Gruta das Cobras.
Agora, naquela ocasião uma cobra caiu sobre o corpo do Venerável Upasena (e ela o
picou). Então, o Venerável Upasena disse para os bhikkhus, “Venham, amigos, levantem
este meu corpo e coloquem-no sobre um estrado e levem-no para fora antes que
ele se disperse aqui mesmo como um punhado de refugo!” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Quando isso foi dito, o
Venerável Sariputta disse para o Venerável Upasena: “Mas nós não vemos nenhuma alteração
no seu corpo ou mudança nas suas faculdades; no entanto, o Venerável Upasena disse,
‘Venham, amigos, levantem este meu corpo e coloquem-no sobre um estrado e
levem-no para fora antes que ele se disperse aqui mesmo como um punhado de
refugo!”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Amigo Sariputta, naquele
que pensa, ‘Eu sou o olho’ ou ‘O olho é meu’; ‘Eu sou o ouvido’ ou ‘O ouvido é
meu’; ‘Eu sou o nariz’ ou ‘O nariz é meu’; ‘Eu sou a língua’ ou ‘A língua é
minha’; ‘Eu sou o corpo’ ou ‘O corpo é meu’; ‘Eu sou a mente’ ou ‘A mente é
minha,’ poderia haver uma alteração no corpo ou uma mudança nas faculdades.
Mas, amigo Sariputta, não me ocorre que ‘Eu sou o olho’ ou ‘O olho é meu’;....
‘Eu sou a mente ou ‘A mente é minha,’ então, porque haveria alguma alteração no
meu corpo, ou alguma mudança nas minhas faculdades?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Deve ser porque a
fabricação de um eu, a fabricação do meu e a tendência subjacente à presunção
já foram desenraizadas no Venerável Upasena há muito tempo, que não lhe ocorre ‘Eu
sou o olho’ ou ‘O olho é meu’; ... ‘Eu sou a mente’ ou ‘A mente é minha.’”<o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, aqueles bhikkhus
levantaram o corpo do Venerável Upasena num estrado e o levaram para fora. E o corpo
do Venerável Upasena ali mesmo se dispersou como um punhado de
refugo. <o:p></o:p></span></p>

