<p class=Normal> “Bhikkhus, há esses quatro tipos de prática Quais quatro? (1) A prática impaciente. (2) A prática paciente. (3) A prática que domestica. (4) A prática que alivia. </p>

<p class=Normal> (1) "Como, bhikkhus, é a prática impaciente? Aqui, alguém não tolera com paciência o frio e o calor; a fome e a sede; moscas, mosquitos, sol e criaturas rastejantes; a linguagem grosseira e ofensiva; ele é incapaz de agüentar as sensações corporais que são dolorosas, torturantes, penetrantes, cortantes, desagradáveis, agonizantes e que ameaçam a vida. Essa é chamada a prática impaciente. </p>

<p class=Normal> (2) "Como é a prática paciente? Aqui, alguém tolera com paciência o frio e o calor; a fome e a sede; moscas, mosquitos, sol e criaturas rastejantes; a linguagem grosseira e ofensiva; ele é capaz de agüentar as sensações corporais que são dolorosas, torturantes, penetrantes, cortantes, desagradáveis, agonizantes e que ameaçam a vida. Essa é chamada a prática paciente. </p>

<p class=Normal> (3) "Como é a prática que domestica? Aqui, ao ver uma forma com o olho, ele não se agarra aos seus sinais ou detalhes. Visto que, se permanecer com a faculdade do olho descuidada, ele será tomado pelos estados ruins e prejudiciais de cobiça e tristeza. Ele pratica a contenção, ele protege a faculdade do olho, ele se empenha na contenção da faculdade do olho. Ao ouvir um som com o ouvido ... Ao cheirar um aroma com o nariz ... Ao saborear um sabor com a língua ... Ao tocar um tangível com o corpo ... Ao conscientizar um objeto mental com a mente, ele não se agarra aos seus sinais ou detalhes. Visto que, se permanecer com a faculdade da mente descuidada, ele será tomado pelos estados ruins e prejudiciais de cobiça e tristeza. Ele pratica a contenção, ele protege a faculdade da mente, ele se empenha na contenção da faculdade da mente. Essa é chamada a prática que domestica. </p>

<p class=Normal> (4) "Como é a prática que alivia? Aqui, ele não tolera um pensamento de desejo sensual que tenha surgido; ele o abandona, o remove, o elimina, o aniquila. Ele não tolera um pensamento de má vontade que tenha surgido ... Ele não tolera um pensamento de crueldade ... Ele não tolera estados ruins e prejudiciais que tenham surgido; ele os abandona, os remove, os elimina, os aniquila. Essa é chamada a prática que alivia." </p>

<p class=Normal>"Esses, bhikkhus são os quatro tipos de prática." </p>

