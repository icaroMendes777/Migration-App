<p class=Normal>  Certa vez o Abençoado<span
style='mso-spacerun:yes'>  </span>estava entre os Bhaggas no Parque do Gamo no
Bosque de Bhesakala, próximo ao Covil dos Crocodilos.<span
style='mso-spacerun:yes'>  </span>Nessa ocasião, o pai de Nakula, o chefe de
família, estava atormentado, sofrendo e gravemente enfermo. Então a mãe de Nakula lhe
disse: &quot;Não fique preocupado se você estiver morrendo, chefe de família. A
morte é dolorosa para quem está preocupado. O Abençoado criticou estar
preocupado no momento da morte. <o:p></o:p></span></p>

<p class=Normal>
&quot;Agora pode ser que você esteja pensando, 'A mãe de Nakula não
será capaz de dar sustento às crianças ou manter a casa depois que eu me for,'
porém você não deveria ver as coisas dessa forma. Eu tenho habilidade em fiar o
algodão, em desemaranhar a lã. Eu posso dar sustento às crianças e manter a
casa depois que você se for. Portanto não fique preocupado se você estiver
morrendo, chefe de família.<span style='mso-spacerun:yes'>  </span>A morte é
dolorosa para quem está preocupado. O Abençoado criticou estar preocupado no
momento da morte. <o:p></o:p></span></p>


<p class=Normal>
&quot;Agora pode ser que você esteja pensando, ‘A mãe de Nakula irá se
casar outra vez depois que eu me for,’ porém você não deveria ver as coisas
dessa forma. Você sabe tão bem quanto eu que a minha fidelidade (lit.,
&quot;celibato do chefe de família&quot;) tem sido constante durante os últimos
dezesseis anos. Portanto não fique preocupado se você estiver morrendo, chefe
de família. A morte é dolorosa para quem está preocupado. O Abençoado criticou
estar preocupado no momento da morte. <o:p></o:p></span></p>


<p class=Normal>
&quot;Agora pode ser que você esteja pensando, ‘A mãe de Nakula não
terá desejo para ir ver o Abençoado, para ir ver a comunidade de bhikkhus,
depois que eu me for,’ porém você não deveria ver as coisas dessa forma. Eu
terei um desejo ainda maior de ir ver o Abençoado, de ir<span
style='mso-spacerun:yes'>  </span>ver a comunidade de bhikkhus, depois que você
se for. Portanto não fique preocupado se você estiver morrendo, chefe de
família. A morte é dolorosa para quem está preocupado. O Abençoado criticou
estar preocupado no momento da morte. <o:p></o:p></span></p>

<p class=Normal>
&quot;Agora pode ser que você esteja pensando, ‘A mãe de Nakula não irá
agir totalmente de acordo com os preceitos depois que eu me for,’ porém você
não deveria ver as coisas dessa forma. Dentre as discípulas chefes de família
vestidas de branco que agem totalmente de acordo com os preceitos, eu sou uma
delas. Se alguém duvidar ou negar isso, que pergunte ao Abençoado, um arahant, perfeitamente iluminado, que está entre os Bhaggas no Parque do Gamo no Bosque de Bhesakala,
próximo ao Covil dos Crocodilos. Portanto não fique preocupado se você estiver
morrendo, chefe de família.<span style='mso-spacerun:yes'>  </span>A morte é
dolorosa para quem está preocupado. O Abençoado criticou estar preocupado no
momento da morte. <o:p></o:p></span></p>


<p class=Normal>
&quot;Agora pode ser que você esteja pensando, ‘A mãe de Nakula não irá
alcançar a tranqüilidade da mente depois que eu me for,’ porém você não deveria
ver as coisas dessa forma.<span style='mso-spacerun:yes'>  </span>Dentre as
discípulas chefes de família vestidas de branco que alcançaram a tranqüilidade
da mente, eu sou uma delas. Se alguém duvidar ou negar isso, que pergunte ao Abençoado,
um arahant, perfeitamente iluminado, que está entre os Bhaggas no Parque do Gamo no Bosque de
Bhesakala, próximo ao Covil dos Crocodilos. Portanto não fique preocupado se
você estiver morrendo, chefe de família.<span style='mso-spacerun:yes'> 
</span>A morte é dolorosa para quem está preocupado. O Abençoado criticou estar
preocupado no momento da morte. <o:p></o:p></span></p>


<p class=Normal>
&quot;Agora pode ser que você esteja pensando, ‘A mãe de Nakula não irá
obter uma base firme nessa Doutrina e Disciplina, não irá obter um bom suporte,
não irá obter consolo, superar as suas dúvidas, dissipar suas perplexidades,
alcançar o destemor ou a independência de outros com relação à mensagem do
Mestre [uma definição padrão daquele ‘que entrou na correnteza’],' porém você
não deveria ver as coisas dessa forma.<span style='mso-spacerun:yes'> 
</span>Dentre as discípulas chefes de família vestidas de branco que obtiveram
uma base firme nessa Doutrina e Disciplina, obtiveram um bom suporte, obtiveram
consolo, superaram as suas dúvidas, dissiparam as suas perplexidades, alcançaram
o destemor e a independência dos outros com relação à mensagem do Mestre, eu sou
uma delas. Se alguém duvidar ou negar isso, que pergunte ao Abençoado, um arahant, perfeitamente iluminado, que está entre os Bhaggas no Parque do Gamo no Bosque de Bhesakala,
próximo ao Covil dos Crocodilos. Portanto não fique preocupado se você estiver
morrendo, chefe de família. A morte é dolorosa para quem está preocupado. O
Abençoado criticou estar preocupado no momento da morte.” <o:p></o:p></span></p>

<p class=Normal>
Enquanto o pai de Nakula estava sendo estimulado com
essa exortação pela mãe de Nakula, sua enfermidade imediatamente amainou. E ele
se recuperou da sua enfermidade. Assim é como a enfermidade do pai de Nakula
foi abandonada. <o:p></o:p></span></p>


<p class=Normal>
Então, pouco tempo depois do pai de Nakula ter se
recuperado da enfermidade, não muito tempo depois da sua recuperação da doença, apoiado em uma
bengala, ele foi até o Abençoado e depois de
cumprimentá-lo sentou a um lado. O Abençoado lhe disse:<o:p></o:p></span></p>


<p class=Normal>
<span style='mso-spacerun:yes'> </span>&quot;É seu ganho, seu grande
ganho, chefe de família, que você tenha a mãe de Nakula - compassiva e
desejando seu bem estar - como sua conselheira e instrutora. Dentre as
discípulas chefes de família vestidas de branco que agem totalmente de acordo
com os preceitos, ela é uma delas.<span style='mso-spacerun:yes'> 
</span>Dentre as discípulas chefes de família vestidas de branco que alcançaram
a tranqüilidade da mente, ela é uma delas. Dentre as discípulas chefes de
família vestidas de branco que obtiveram uma base firme nessa Doutrina e
Disciplina, obtiveram um bom suporte, obtiveram consolo, superaram as suas
dúvidas, dissiparam as suas perplexidades, alcançaram o destemor e a
independência dos outros com relação à mensagem do Mestre, ela é uma delas. É
seu ganho, seu grande ganho, chefe de família, que você tenha a mãe de Nakula -
compassiva e desejando seu bem estar - como sua conselheira e instrutora.&quot;
