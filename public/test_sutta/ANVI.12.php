<p class=Normal> "Bhikkhus, há esses seis princípios de cordialidade que criam amor e respeito e conduzem à coesão, à não-disputa, à concórdia, e à união. Quais seis? </p>

<p class=Normal> (1) "Aqui, um bhikkhu pratica, tanto em público como em particular, ações com amor bondade com o corpo para com os seus companheiros na vida santa. Esse é um princípio de cordialidade que cria amor e respeito e conduz à coesão, à não-disputa, à concórdia, e à união. </p>

<p class=Normal> (2) "Outra vez, um bhikkhu pratica, tanto em público como em particular, ações com amor bondade com a linguagem para com os seus companheiros na vida santa. Esse é um princípio de cordialidade que cria amor e respeito e conduz à coesão, à não-disputa, à concórdia, e à união. </p>

<p class=Normal> (3) "Outra vez, um bhikkhu pratica, tanto em público como em particular, ações com amor bondade com a mente para com os seus companheiros na vida santa. Esse é um princípio de cordialidade que cria amor e respeito e conduz à coesão, à não-disputa, à concórdia, e à união. </p>

<p class=Normal> (4) “Outra vez, um bhikkhu comparte as coisas com os seus companheiros virtuosos na vida santa; sem fazer qualquer reserva, ele comparte com eles tudo que seja ganho que esteja de acordo com o Dhamma, e que tenha sido obtido de uma maneira de acordo com o Dhamma, incluindo até mesmo o conteúdo da sua tigela de alimentos. Esse é um princípio de cordialidade que cria amor e respeito e conduz à coesão, à não-disputa, à concórdia, e à união. </p>

<p class=Normal> (5) “Outra vez, um bhikkhu permanece, tanto em público como em particular, possuindo, juntamente com os seus companheiros da vida santa, aquelas virtudes que são intactas, não-laceradas, imaculadas, não-matizadas, libertadoras, elogiadas pelos sábios e que conduzem à concentração. Esse é um princípio de cordialidade que cria amor e respeito e conduz à coesão, à não-disputa, à concórdia, e à união. </p>

<p class=Normal> (6) “Outra vez, um bhikkhu permanece, tanto em público como em particular, possuindo, juntamente com os seus companheiros da vida santa, aquele entendimento que é nobre, que emancipa, e que conduz aquele que pratica em conformidade com esse entendimento, à completa destruição do sofrimento. Esse é um princípio de cordialidade que cria amor e respeito e conduz à coesão, à não-disputa, à concórdia, e à união. </p>

<p class=Normal>"Esses, bhikkhus, são os seis princípios de cordialidade que criam amor e respeito e conduzem à coesão, à não-disputa, concórdia e união." </p>


