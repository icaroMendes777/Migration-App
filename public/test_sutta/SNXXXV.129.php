<p class=Normal> Em certa ocasião, o venerável Ananda stava em Kosambi, no parque de Ghosita. Então o chefe de família Ghosita foi até o venerável Ananda e depois de cumprimentá-lo sentou a um lado e disse: </p>

<p class=Normal> “Venerável Ananda, é dito 'diversidade de elementos, diversidade de elementos.' De que modo, venerável senhor, o Abençoado falou em diversidade de elementos?" </p>

<p class=Normal> "Chefe de família, há o elemento olho e formas que são agradáveis, e a consciência no olho: na dependência do contato para serem experimentadas como prazerosas, uma sensação prazerosa surge. Há o elemento olho e formas que são desagradáveis, e a consciência no olho: na dependência do contato para serem experimentadas como desprazerosas, uma sensação desprazerosa surge. Há o elemento olho e formas que são a base para a equanimidade, e a consciência no olho: na dependência do contato para serem experimentadas como nem prazerosas nem desprazerosas, uma sensação nem prazerosa nem desprazerosa surge. </p>

<p class=Normal> "Chefe de família, há o elemento ouvido e sons ... o elemento mente e objetos mentais que são agradáveis, e a consciência na mente: na dependência do contato para serem experimentados como prazerosos, uma sensação prazerosa surge. Há o elemento mente e objetos mentais que são desagradáveis, e a consciência na mente: na dependência do contato para serem experimentados como desprazerosos, uma sensação desprazerosa surge. Há o elemento mente e objetos mentais que são a base para a equanimidade, e a consciência na mente: na dependência do contato para serem experimentados como nem prazerosos nem desprazerosos, uma sensação nem prazerosa nem desprazerosa surge. </p>

<p class=Normal> "Chefe de família, é desse modo que o Abençoado falou em diversidade de elementos." </p>

