<p class=Normal> “Bhikkhus, eu ensinarei para vocês o Dhamma para abandonar o todo através do conhecimento direto e da completa compreensão. Ouçam e prestem muita atenção àquilo que eu vou dizer.” – “Sim, venerável senhor,” os bhikkhus responderam. O Abençoado disse o seguinte:</p>

<p class=Normal> “E qual, bhikkhus, é o Dhamma para abandonar o todo através do conhecimento direto e da completa compreensão? O olho deve ser abandonado através do conhecimento direto e da completa compreensão, as formas devem assim ser abandonadas, a consciência deve assim ser abandonada, o contato no olho deve assim ser abandonado e qualquer sensação que surja tendo o contato no olho como condição – quer seja prazerosa, dolorosa ou nem prazerosa, nem dolorosa – isso também deve ser abandonado através do conhecimento direto e da completa compreensão.</p>

<p class=Normal> “O ouvido deve ser abandonado através do conhecimento direto e da completa compreensão, os sons devem assim ser abandonados ... A mente deve ser abandonada através do conhecimento direto e da completa compreensão, os objetos mentais devem assim ser abandonados, a consciência na mente deve assim ser abandonada, o contato na mente deve assim ser abandonado e qualquer sensação que surja tendo o contato na mente como condição – quer seja prazerosa, dolorosa ou nem prazerosa, nem dolorosa – isso também deve ser abandonado através do conhecimento direto e da completa compreensão.</p>

<p class=Normal> “Esse, bhikkhus, é o Dhamma para abandonar o todo através do conhecimento direto e da completa compreensão.”</p>

