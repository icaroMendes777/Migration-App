<p class=Normal><a name=R1></a>"Bhikkhus, eu conheci pessoalmente duas coisas: não contentamento com as qualidades benéficas [<a href="#N1">1</a>] e infatigabilidade no esforço.[<a href="#N2">2</a>] Infatigável [decidido] me esforcei e com satisfação deixaria que a carne e o sangue do meu corpo secasse, deixando só pele, tendões e ossos, sem relaxar na minha energia enquanto não tivesse atingido o que pode ser atingido através da firmeza humana, da energia humana e do esforço humano.[<a href="#N3">3</a>] Foi através da diligência que realizei a iluminação, bhikkhus; foi através da diligência que consegui a insuperável segurança contra o cativeiro.[<a href="#N4">4</a>]</p>

<p class=Normal>"Bhikkhus, se vocês também se esforçarem infatigáveis [decididos]: 'Com satisfação que a carne e o sangue do meu corpo sequem, deixando só pele, tendões e ossos, não relaxarei na minha energia enquanto não tiver atingido o que pode ser atingido através da firmeza humana, da energia humana e do esforço humano,' vocês também irão, em pouco tempo, alcançar e permanecer no objetivo supremo da vida santa, pelo qual membros de um clã deixam a vida em família pela vida santa, tendo conhecido e realizado por si mesmos no aqui e agora, portanto, bhikkhus, vocês devem treinar assim: 'Com satisfação que a carne e o sangue do meu corpo seque, deixando só pele, tendões e ossos, não relaxarei na minha energia enquanto não tiver atingido o que pode ser atingido através da firmeza humana, da energia humana e do esforço humano.' Assim é como vocês devem treinar."</p>

