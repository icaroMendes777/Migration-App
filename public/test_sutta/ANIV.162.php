<p class=Normal> “Bhikkhus, há esses quatro tipos de prática Quais quatro? (1) A prática que é dolorosa, com conhecimento direto lento. (2) A prática que é dolorosa, com conhecimento direto rápido. (3) A prática que é prazerosa, com conhecimento direto lento. (4) A prática que é prazerosa, com conhecimento direto rápido. </p>

<p class=Normal><a name=R1></a> (1) "Como, bhikkhus, é a prática que é dolorosa, com conhecimento direto lento? Aqui, alguém por natureza tende com intensidade para a cobiça e ele com frequência experimenta sofrimento e tristeza nascidos da cobiça. Por natureza ele tende com intensidade para a raiva e ele com frequência experimenta sofrimento e tristeza nascidos da raiva. Por natureza ele tende com intensidade para a delusão e ele com frequência experimenta sofrimento e tristeza nascidos da delusão. Estas cinco faculdades nele surgem com debilidade: as faculdades da fé, energia, atenção plena, concentração, e sabedoria. Porque essas faculdades são débeis, ele realiza com lentidão a condição próxima para a destruição das impurezas. [<a href="#N1">1</a>] Essa é chamada a prática que é dolorosa, com conhecimento direto lento. </p>

<p class=Normal> (2) "Como é a prática que é dolorosa, com conhecimento direto rápido? Aqui, alguém por natureza tende com intensidade para a cobiça ... raiva ... delusão e ele com frequência experimenta sofrimento e tristeza nascidos da delusão. Estas cinco faculdades nele surgem com proeminência: as faculdades da fé, energia, atenção plena, concentração, e sabedoria. Porque essas faculdades são proeminentes, ele realiza com rapidez a condição próxima para a destruição das impurezas. Essa é chamada a prática que é dolorosa, com conhecimento direto rápido. </p>

<p class=Normal> (3) " Como é a prática que é prazerosa, com conhecimento direto lento? Aqui, alguém por natureza não tende com intensidade para a cobiça e ele com frequência não experimenta sofrimento e tristeza nascidos da cobiça. Por natureza ele não tende com intensidade para a raiva e ele com frequência não experimenta sofrimento e tristeza nascidos da raiva. Por natureza ele não tende com intensidade para a delusão e ele com frequência não experimenta sofrimento e tristeza nascidos da delusão. Estas cinco faculdades nele surgem com debilidade: as faculdades da fé, energia, atenção plena, concentração, e sabedoria. Porque essas faculdades são débeis, ele realiza com lentidão a condição próxima para a destruição das impurezas. Essa é chamada a prática que é prazerosa, com conhecimento direto lento. </p>

<p class=Normal> (4) "Como é a prática que é prazerosa, com conhecimento direto rápido? Aqui, alguém por natureza não tende com intensidade para a cobiça ... raiva ... delusão e ele com frequência não experimenta sofrimento e tristeza nascidos da delusão. Estas cinco faculdades nele surgem com proeminência: as faculdades da fé, energia, atenção plena, concentração, e sabedoria. Porque essas faculdades são proeminentes, ele realiza com rapidez a condição próxima para a destruição das impurezas. Essa é chamada a prática que é prazerosa, com conhecimento direto rápido. </p>

<p class=Normal>"Esses, bhikkhus são os quatro tipos de prática." </p>
