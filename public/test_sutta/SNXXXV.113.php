<p class=Normal> Assim ouvi. Em certa ocasião o Abençoado estava em Natika na Casa de Tijolos. Então, enquanto o Abençoado estava só em isolamento ele proferiu esta exposição do Dhamma: </p>

<p class=Normal> "Na dependência do olho e das formas, a consciência no olho surge. O encontro dos três é o contato. Com o contato como condição, a sensação [surge]; com a sensação como condição, o desejo; com o desejo como condição, o apego ... Essa é a origem de toda essa massa de sofrimento. </p>

<p class=Normal> “Na dependência do ouvido e dos sons ... na dependência da mente e dos objetos mentais, a consciência na mente surge. O encontro dos três é o contato. Com o contato como condição, a sensação [surge]; com a sensação como condição, o desejo; com o desejo como condição, o apego ... Essa é a origem de toda essa massa de sofrimento. </p>

<p class=Normal> "Na dependência do olho e das formas, a consciência no olho surge. O encontro dos três é o contato. Com o contato como condição, a sensação [surge]; com a sensação como condição, o desejo. Mas com o desaparecimento e cessação sem deixar vestígios desse mesmo desejo, cessa o apego; com a cessação do apego, cessa o ser/existir ... Essa é a cessação de toda essa massa de sofrimento. </p>

<p class=Normal> “Na dependência do ouvido e dos sons ... na dependência da mente e dos objetos mentais, a consciência na mente surge. O encontro dos três é o contato. Com o contato como condição, a sensação [surge]; com a sensação como condição, o desejo. Mas com o desaparecimento e cessação sem deixar vestígios desse mesmo desejo, cessa do apego; com a cessação do apego, a cessação do ser/existir ... Essa é a cessação de toda essa massa de sofrimento.” </p>

<p class=Normal> Agora naquela ocasião um certo bhikkhu estava parado ouvindo o Abençoado. O Abençoado o viu ali parado ouvindo e disse: "Você ouviu essa exposição do Dhamma, bhikkhu?" </p>

<p class=Normal> "Sim, venerável senhor." </p>

<p class=Normal> "Bhikkhu, aprenda, obtenha proficiência e lembre-se dessa exposição do Dhamma. Essa exposição do Dhamma traz benefícios e faz parte dos fundamentos da vida santa." </p>

