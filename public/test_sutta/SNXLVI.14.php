<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Certa
ocasião, o Abençoado estava em Rajagaha, no Bambual, no Santuário dos Esquilos.
Naquele ocasião, o Venerável Maha Kassapa estava na Caverna Pipphali – doente,
aflito, gravemente enfermo. Então, ao anoitecer, o Abençoado saiu do seu
isolamento e foi até o ven. Maha Kassapa. Ao chegar ele sentou num assento que
havia sido preparado e disse:<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Eu espero
que você esteja agüentando bem, Kassapa, eu espero que você esteja melhorando.
Espero que as suas sensações de dor estejam diminuindo e não aumentando e que a
sua diminuição, não o seu aumento, seja evidente.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Venerável
senhor, eu não estou agüentando bem, eu não estou melhorando. Minhas sensações
de dor estão aumentando, não diminuindo, o seu aumento, não a sua diminuição é
evidente.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Esses sete
fatores da iluminação, Kassapa, foram bem expostos por mim; quando
desenvolvidos e cultivados, eles conduzem ao conhecimento direto, à iluminação,
a Nibbana. Quais sete? O fator da iluminação da atenção plena foi bem exposto
por mim; quando desenvolvido e cultivado, ele conduz ao conhecimento direto, à
iluminação, a Nibbana ... o fator da iluminação da equanimidade foi bem exposto
por mim; quando desenvolvido e cultivado, ele conduz ao conhecimento direto, à
iluminação, a Nibbana. Esses sete fatores da iluminação, Kassapa, foram bem
expostos por mim; quando desenvolvidos e cultivados, eles conduzem ao
conhecimento direto, à iluminação, a Nibbana.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Com
certeza, Abençoado, esses são fatores da iluminação! Com certeza, Iluminado,
esses são fatores da iluminação!”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Isso foi o
que disse o Abençoado. O Venerável Maha Kassapa ficou satisfeito e contente com
as palavras do<span style="mso-spacerun: yes">  </span>Abençoado. E o Venerável
Maha Kassapa se recuperou daquela enfermidade. Dessa maneira o Venerável Maha
Kassapa foi curado da sua enfermidade.<o:p></o:p></span></p>


