<p class=Normal><a name=R1></a> "Bhikkhus, alguém que possua quatro qualidades pode ser considerado como uma pessoa falsa. [<a href="#N1">1</a>] Quais quatro? </p>

<p class=Normal> (1) "Aqui, bhikkhus, uma pessoa falsa revela os defeitos dos outros mesmo quando não é perguntada, tanto mais então quando é perguntada. Mas ao ser perguntada, então, estimulada pelas perguntas, ela fala sobre os defeitos dos outros sem omitir ou deixar nada de lado, de modo completo e em detalhe. Assim pode ser entendido: 'Esse fulano é uma pessoa falsa.'</p>

<p class=Normal> (2) "Novamente, uma pessoa falsa não revela as virtudes dos outros mesmo quando  é perguntada, tanto menos então quando não é perguntada. Mas ao ser perguntada, então, estimulada pelas perguntas, ela fala sobre as virtudes dos outros com omissões e deixando algo de lado, de modo incompleto e sem detalhes. Assim pode ser entendido: 'Esse fulano é uma pessoa falsa.'</p>

<p class=Normal> (3) "Novamente, uma pessoa falsa não revela os seus defeitos mesmo quando  é perguntada, tanto menos então quando não é perguntada. Mas ao ser perguntada, então, estimulada pelas perguntas, ela fala sobre os seus defeitos com omissões e deixando algo de lado, de modo incompleto e sem detalhes. Assim pode ser entendido: 'Esse fulano é uma pessoa falsa.'</p>

<p class=Normal> (4) "Novamente, uma pessoa falsa revela as suas virtudes mesmo quando não é perguntada, tanto mais então quando é perguntada. Mas ao ser perguntada, então, estimulada pelas perguntas, ela fala sobre as suas virtudes sem omitir ou deixar nada de lado, de modo completo e em detalhe. Assim pode ser entendido: 'Esse fulano é uma pessoa falsa.'</p>

<p class=Normal> "Alguém que possua essas quatro qualidades pode ser considerado como uma pessoa falsa." </p>

<p class=Normal> "Bhikkhus, alguém que possua outras quatro qualidades pode ser considerado como uma pessoa verdadeira. Quais quatro? </p>

<p class=Normal> (1) "Aqui, bhikkhus, uma pessoa verdadeira não revela os defeitos dos outros mesmo quando  é perguntada, tanto menos então quando não é perguntada. Mas ao ser perguntada, então, estimulada pelas perguntas, ela fala sobre os defeitos dos outros com omissões e deixando algo de lado, de modo incompleto e sem detalhes. Assim pode ser entendido: 'Esse fulano é uma pessoa verdadeira.'</p>

<p class=Normal> (2) "Novamente, uma pessoa verdadeira revela as virtudes dos outros mesmo quando não é perguntada, tanto mais então quando é perguntada. Mas ao ser perguntada, então, estimulada pelas perguntas, ela fala sobre as virtudes dos outros sem omitir ou deixar nada de lado, de modo completo e em detalhe. Assim pode ser entendido: 'Esse fulano é uma pessoa verdadeira.'</p>

<p class=Normal> (3) " Novamente, uma pessoa verdadeira revela os seus defeitos mesmo quando não é perguntada, tanto mais então quando é perguntada. Mas ao ser perguntada, então, estimulada pelas perguntas, ela fala sobre os seus defeitos sem omitir ou deixar nada de lado, de modo completo e em detalhe. Assim pode ser entendido: 'Esse fulano é uma pessoa verdadeira.'</p>

<p class=Normal> (4) "Novamente, uma pessoa verdadeira não revela as suas virtudes mesmo quando  é perguntada, tanto menos então quando não é perguntada. Mas ao ser perguntada, então, estimulada pelas perguntas, ela fala sobre as suas virtudes com omissões e deixando algo de lado, de modo incompleto e sem detalhes. Assim pode ser entendido: 'Esse fulano é uma pessoa verdadeira.'</p>

<p class=Normal> "Alguém que possua essas quatro qualidades pode ser considerado como uma pessoa verdadeira." </p>

<p class=Normal> "Bhikkhus, quando uma noiva chega à sua nova casa, quer seja à noite ou durante o dia, primeiro ela estabelece uma aguçada noção de vergonha e temor moral com relação à sogra, ao sogro, ao marido, e até mesmo com relação aos escravos, trabalhadores e serviçais. Mas passado algum tempo, como resultado da convivência e da intimidade, ela diz para a sogra, para o sogro, e para o marido: 'Vá embora! Você não sabe nada!'</p>

<p class=Normal> "Do mesmo modo, quando algum bhikkhu segue a vida santa, quer seja à noite ou durante o dia, primeiro ele estabelece uma aguçada noção de vergonha e temor moral com relação aos bhikkhus, às bhikkhunis, aos discípulos leigos e às discípulas leigas, e até mesmo com relação aos trabalhadores do monastério e os noviços. Mas passado algum tempo, como resultado da convivência e da intimidade, ele diz até mesmo para o seu mestre e preceptor: 'Vá embora! Você não sabe nada!'</p>

<p class=Normal> "Portanto, bhikkhus, vocês devem treinar assim: 'Nós permaneceremos com a mente igual à noiva quando chega à sua casa nova.' É dessa forma que vocês devem treinar." </p>

