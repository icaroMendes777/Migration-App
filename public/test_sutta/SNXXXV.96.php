<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Bhikkhus,
eu ensinarei para vocês acerca daquele que está sujeito ao declínio, acerca
daquele que não está sujeito ao declínio e acerca das seis bases dominadas.
Ouçam e prestem muita atenção àquilo que eu vou dizer.” – “Sim, venerável
senhor,” os bhikkhus responderam. O Abençoado disse o seguinte: <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como,
bhikkhus, alguém está sujeito ao declínio? Aqui, bhikkhus, quando um bhikkhu
viu uma forma com o olho, surgem nele estados ruins e prejudiciais, memórias e
intenções conectadas com os grilhões. Se um bhikkhu tolerá-los e não os
abandonar, não os remover, não os eliminar e não os aniquilar, ele deve
entender isso da seguinte forma: ‘Eu estou declinando dos estados benéficos.
Pois isto foi chamado de declínio pelo Abençoado.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Além
disso, bhikkhus, quando um bhikkhu ouviu um som com o ouvido ... percebeu
objetos mentais com a mente, surgem nele estados ruins e prejudiciais, memórias
e intenções conectadas com os grilhões. Se um bhikkhu tolerá-los e não os
abandonar, não os remover, não os eliminar e não os aniquilar, ele deve
entender isso da seguinte forma: ‘Eu estou declinando dos estados benéficos.
Pois isto foi chamado de declínio pelo Abençoado.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“É dessa
forma, bhikkhus, que alguém está sujeito ao declínio.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E como,
bhikkhus, alguém não está sujeito ao declínio? Aqui, bhikkhus, quando um
bhikkhu viu uma forma com o olho, surgem nele estados ruins e prejudiciais,
memórias e intenções conectadas com os grilhões. Se um bhikkhu não os tolerar,
mas abandoná-los, removê-los, eliminá-los e aniquilá-los, ele deve entender
isso da seguinte maneira: ‘Eu não estou declinando dos estados benéficos. Pois
isto foi chamado de não declínio pelo Abençoado.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Além
disso, bhikkhus, quando um bhikkhu ouviu um som com o ouvido ... percebeu
objetos mentais com a mente, surgem nele estados ruins e prejudiciais, memórias
e intenções conectadas com os grilhões. Se um bhikkhu não os tolerar, mas
abandoná-los, removê-los, eliminá-los e aniquilá-los, ele deve entender isso da
seguinte forma: ‘Eu não estou declinando dos estados benéficos. Pois isto foi
chamado de não declínio pelo Abençoado.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“É desse
modo, bhikkhus, que alguém não está sujeito ao declínio. <o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“E o que,
bhikkhus, são as seis bases dominadas? Aqui, bhikkhus, quando um bhikkhu viu
uma forma com o olho, não surgem nele estados ruins e prejudiciais, nem
quaisquer memórias e intenções conectadas com os grilhões. O bhikkhu deve
entender isso da seguinte maneira: ‘Essa base foi dominada. Pois isso foi
chamado de base dominada pelo Abençoado.’<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Além
disso, bhikkhus, quando um bhikkhu ouviu um som com o ouvido ... percebeu
objetos mentais com a mente, não surgem nele estados ruins e prejudiciais, nem
quaisquer memórias e intenções conectadas com os grilhões. O bhikkhu deve
entender isso da seguinte maneira: ‘Essa base foi dominada. Pois isso foi
chamado de base dominada pelo Abençoado.’ Essas, bhikkhus, são chamadas as seis
bases dominadas.”<o:p></o:p></span></p>


