<p class=Normal> "Bhikkhus, possuindo cinco qualidades, um bhikkhu demonstra compaixão com as pessoas leigas. Quais cinco? (1) Ele as encoraja no comportamento virtuoso. (2) Ele as estabelece na visão do Dhamma. (3) Quando elas estão enfermas ele as visita e lhes estimula a atenção plena dizendo: 'Que os honoráveis estabeleçam a atenção plena naquilo que é digno.' (4) Quando uma grande sangha de bhikkhus chega ao monastério incluindo bhikkhus de várias regiões ele se dirige às pessoas leigas e as informa: 'Amigos, uma grande sangha de bhikkhus chegou ao monastério incluindo bhikkhus de várias regiões. Façam mérito. Essa é uma ocasião para fazer mérito.' (5) Ele mesmo se alimenta com qualquer comida que lhe derem, quer seja grosseira ou excelente; ele não desperdiça aquilo que foi dado com base na fé. Possuindo essas cinco qualidades, um bhikkhu demonstra compaixão com as pessoas leigas."</p>

