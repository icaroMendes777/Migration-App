<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Em
Savatthi. Então, ao amanhecer, a bhikkhuni Kisagotami se vestiu
e tomando a tigela e o manto externo foi para Savatthi esmolar alimentos.
Depois de haver esmolado alimentos em Savatthi e de haver retornado, após a
refeição, ela se dirigiu ao bosque dos Homens Cegos em busca de isolamento. <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então Mara, o Senhor do
Mal, desejando despertar medo, trepidação e terror na bhikkhuni Kisagotami,
desejando que ela abandonasse a sua concentração, se aproximou e se dirigiu a
ela em versos: <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
 “Por que agora, quando o seu filho está morto,<br>
você senta sozinha com o rosto choroso?<br>
Entrando sozinha na floresta,<br>
você está em busca de um homem?” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Foi então que a bhikkhuni
Kisagotami pensou: “Agora, quem será que recitou esses versos – um ser humano
ou um ser não humano?” Então lhe ocorreu: “Foi Mara, o Senhor do Mal, quem
recitou esses versos desejando despertar medo, trepidação e terror em mim,
desejando que eu abandone a minha concentração.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, a bhikkhuni
Kisagotami, tendo compreendido, “Este é Mara, o Senhor do Mal,” respondeu em
versos: <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
 “Superei a morte de filhos;<br>
com isso, a busca por homens se acabou.<br>
Não sofro, não choro,<br>
nem você eu temo, amigo. <o:p></o:p></span></p>

<p class=Normal style='margin-left:.75in'>
O deleite em todos os
lugares foi destruído,<br>
a massa da escuridão foi rompida.<br>
Tendo conquistado o exército da Morte,<br>
permaneço livre das impurezas que contaminam.” <o:p></o:p></span></p>

<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, Mara, o Senhor do
Mal, compreendendo que, “A bhikkhuni Kisagotami sabe quem sou,” triste e
desapontado, desapareceu no mesmo instante. <o:p></o:p></span></p>



