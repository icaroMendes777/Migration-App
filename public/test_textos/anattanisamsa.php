href="../glossario.php#anatta">anatta<span style='font-style:normal'>.</span></a></i>
Alguém que perceba com clareza a característica de <i>anatta</i> alcançará o
estágio de conhecimento de um <i>sotapatti magga </i>(caminho daquele que
entrou na correnteza) em que <i>atta-ditthi </i>(ilusão de um ego) ou <i>sakkaya-ditthi
</i>(idéia da existência de um eu) são totalmente erradicadas.<o:p></o:p></span></p>

<p class=Normal><b>Realização de Anatta e
Kammas Passados</b> <o:p></o:p></span></b></p>

<p class=Normal>Todos os seres que vagueiam
e perambulam pelo longo ciclo de renascimentos que não tem um princípio
definido, denominado <i>samsara,</i> raramente encontram a <i><a
href="../glossario.php#sasana">Sasana</a></i> de um Buda. Eles não encontram a <i>Sasana
</i>de um Buda nem mesmo ao longo de cem mil ciclos cósmicos. Eles não têm a
oportunidade de encontrar a <i>Sasana </i>de um Buda nem mesmo uma vez ao longo
de um número infinito de ciclos cósmicos. Predominam o número de existências e o
número de ciclos cósmicos em que eles foram atormentados pelo mal e pelos
erros, por conseguinte, na constituição mental desses seres existe a todo
momento um número incontável de <i>kammas</i> que podem resultar na
possibilidade deles serem arremessados no inferno de Sanjiva e no resto dos
outros infernos, ou<span style="mso-spacerun: yes">  </span>ainda, deles
renascerem em outras existências como um peta, asura ou no reino animal.<span
style="mso-spacerun: yes">  </span><o:p></o:p></span></p>

<p class=Normal>Atta-ditthi </span></i><span
lang=PT-BR style='mso-ansi-language:PT-BR'>é o cabeça – o chefe por assim dizer
– dos antigos <i><a href="../glossario.php#akusala">kammas akusala</a></i> que
acompanham os seres incessantemente. Enquanto existir <i>sakkaya-ditthi</i>,
esses <i>akusala kamma </i>antigos terão força e vigor. Embora os seres possam
desfrutar da felicidade e prosperidade como <i>devas </i>ou como <i>Sakka</i>
nos seis <i><a href="../glossario.php#deva">deva</a> <a
href="../glossario.php#loka">lokas</a></i>, eles estarão forçados a existir
sempre com os olhos voltados<span style="mso-spacerun: yes">  </span>para os
quatro <i><a href="../glossario.php#apaya_bhumi">apaya lokas</a>.</i> Da mesma
forma, embora os seres possam desfrutar da felicidade e prosperidade no <i><a
href="../glossario.php#rupa">rupa</a> brahma </i>e <i>arupa brahma lokas, </i>eles
estarão forçados a existir sempre com os olhos voltados<span
style="mso-spacerun: yes">  </span>para os quatro <i>apaya lokas.<o:p></o:p></i></span></p>

<p class=Normal>Os frutos de uma palmeira
possuem a tendência sempre presente de cair no solo mesmo estando firmemente
pegados no topo das palmeiras. Enquanto os caules estiverem firmes os frutos
permanecerão nas palmeiras, mas na medida em que os caules enfraquecem,
inevitavelmente os frutos caem no chão. Da mesma forma, <i>devas </i>e <i>brahmas
</i>atormentados com <i>atta ditthi </i>têm a oportunidade de existir<span
style="mso-spacerun: yes">  </span>no <i>deva </i>e <i>brahma lokas </i>apenas
enquanto os ‘caules’ das forças vitais como <i>devas </i>e <i>brahmas </i>permanecerem
intactos. Quando esses ‘caules’ das forças vitais forem cortados, os seres inevitavelmente
descerão para os <i>lokas </i>inferiores, exatamente como os frutos das
palmeiras. Isso, de fato,<span style="mso-spacerun: yes">  </span>é assim
porque <i>sakkaya-ditthi, </i>que está sempre presente na constituição mental
de um ser, é um grande fardo ainda mais pesado que o grande Monte Meru,
considerando que <i>sakkaya-ditthi </i>abrange um número incontável de <i>akusala
kammas.<o:p></o:p></i></span></p>

<p class=Normal>Portanto, os seres em cuja
constituição mental exista <i>sakkaya-ditthi </i>são continuamente forçados a
cair ou descer para os <i>apaya lokas </i>mesmo que estejam vivendo nos mais
elevados <i>brahma lokas. </i>A situação daqueles seres que se encontram nos <i>brahma
lokas </i>inferiores ou nos <i>deva lokas </i>ou no plano humano é muito pior e
carece de comentário adicional. Embora os seres possam estar existindo como
reis dos <i>brahma </i>ou reis dos <i>devas <span style="mso-spacerun:
yes"> </span></i>a sua constituição mental possui, já pronta, um sem número de
infernos inferiores, os mundos dos <i><a href="../glossario.php#peta">petas</a></i>,
os mundos dos <i><a href="../glossario.php#asura">asuras</a></i> e os mundos
animais. É<span style="mso-spacerun: yes">  </span>por desconhecer que a sua
constituição mental possui sempre presente a tendência para esses reinos
inferiores e miseráveis que esses seres, como os reis dos <i>brahmas </i>e os
reis dos <i>devas, </i>são capazes de derivar prazer e satisfação dessas
existências.<o:p></o:p></span></p>

<p class=Normal>Todos os antigos <i>akusala
kamma </i>que desde um princípio indeterminado têm acompanhado os seres nesse
ciclo de renascimentos, longo e sem um início definido, chamado <i>samsara,</i>
são completamente extintos assim que <i>sakkaya-ditthi, </i>que é o seu cabeça,
desaparece completamente.<o:p></o:p></span></p>

<p class=Normal>Para não falar daqueles <i>akusala
kamma </i>que têm acompanhado os seres desde existências<span
style="mso-spacerun: yes">  </span>e ciclos cósmicos anteriores e até mesmo o
número incontável de <i>akusala kamma </i>praticados na existência atual; k<i>ammas
</i>como matar e roubar cuja tendência resultante desaparece inteiramente assim
que <i>sakkaya-ditthi</i> for completamente extinto. Podem restar ocasiões em
que esses seres temam a destruição de parasitas e insetos, mas não existirá
mais motivo para que eles tenham medo das conseqüências do número incontável de
<i>akusala kamma </i>passados.<o:p></o:p></span></p>

<p class=Normal>Seres cuja constituição
mental esteja totalmente livre de <i>sakkaya-ditthi </i>possuem os olhos
voltados para os planos superiores dos <i>deva </i>e <i>brahma lokas</i> mesmo
que estejam vivendo no mundo humano. Embora possam estar vivendo nos planos inferiores
dos <i>devas </i>e <i>brahmas</i>, os seus olhos estarão para sempre voltados
para os planos superiores dos <i>devas </i>e <i>brahmas. </i>Eles se assemelham
aos vapores que<span style="mso-spacerun: yes">  </span>sobem continuamente das
florestas e montanhas durante o final da estação das chuvas.<o:p></o:p></span></p>

<p class=Normal>Isto demonstra a
grandiosidade das vantagens que resultam da extinção de <i>sakkaya-ditthi </i>no
que diz respeito a <i>kammas </i>passados.<o:p></o:p></span></p>

<p class=Normal><b>Realização de Anatta e
Kammas Futuros</b><o:p></o:p></span></b></p>

<p class=Normal>Os seres humanos, <i>devas </i>e
<i>brahmas, </i>que possuem <i>sakkaya-ditthi </i>como parte da sua
constituição mental podem ser seres bons e virtuosos hoje, mas podem cometer um
incontável número de <i>duccaritas </i>(N.T.: conduta má: ações, linguagem e
pensamentos), tal como os grandes <i>panatipata kammas </i>de matricídio,
parricídio, ou matar arahants, ou os <i>adinnadana kamma </i>de roubar, etc.,
amanhã, ou no dia seguinte, ou num outro mês, ou no próximo ano, ou nas
existências seguintes. Pode ser que hoje eles vivam numa congregação da <i>Sasana
</i>do Buda, mas amanhã, ou no dia seguinte, etc., eles podem estar fora da <i>Sasana
</i>do Buda e podem até mesmo se converter em destruidores da <i>Sasana.<o:p></o:p></i></span></p>

<p class=Normal>Os seres humanos, <i>devas </i>e
<i>brahmas, </i>que no entanto percebem bem a característica de <i>anatta,</i>
e que dessa forma extirparam <i>sakkaya-ditthi </i>completamente da sua
constituição mental, não cometem mais as <i>duccaritas </i>e outros <i>akusala
kamma, </i>nem mesmo em sonhos,<i> </i>a partir do momento em que eles se
livraram de <i>sakkaya-ditthi</i>, embora possam continuar<span
style="mso-spacerun: yes">  </span>perambulando pelo <i>samsara </i>durante
muitas existências e muitos ciclos mais. A partir do dia em que eles se
livrarem de <i>sakkaya-ditthi </i>e até o momento em que realizarem<span
style="mso-spacerun: yes">  </span><i>Nibbana</i>, eles permanecerão como parte
da <i>Sasana </i>do Buda de forma permanente e contínua ao longo de existências
e ciclos cósmicos sucessivos. Para eles não existe existência ou mundo no qual
a <i>Sasana </i>do Buda tenha desaparecido.<o:p></o:p></span></p>

<p class=Normal>Isto demonstra a
grandiosidade das vantagens que resultam da extinção de <i>sakkaya-ditthi </i>no
que diz respeito a <i>kammas </i>futuros.<o:p></o:p></span></p>

<p class=Normal><b>Como os Kammas Passados
se tornam Inoperantes</b><o:p></o:p></span></b></p>

<p class=Normal>Como o incontável número
de<span style="mso-spacerun: yes">  </span><i>kammas </i>passados se tornam
inoperantes no momento em que <i>sakkaya-ditthi </i>é extinto pode ser
ilustrado da seguinte forma: <o:p></o:p></span></p>

<p class=Normal>Num cordão com um número
incontável de contas presas, enfiadas num fio de seda resistente, se uma conta
for puxada, todas as demais seguem ou acompanham aquela que foi puxada. Mas se
o fio de seda for removido, ao puxar uma das contas as demais não serão
afetadas,<span style="mso-spacerun: yes">  </span>porque já não existe conexão
entre elas.<o:p></o:p></span></p>

<p class=Normal>Um ser de posse de <i>sakkaya-ditthi
</i>abriga um grande apego pelos <i>khandas </i>ao longo das existências e
ciclos cósmicos passados transformando-os em ‘eu’. Ao pensar ‘Em existências e
ciclos cósmicos passados eu fui um ser humano em muitas ocasiões, um <i>deva</i>
ou um <i>brahma,’</i> ele adquire o ‘fio de seda’ que é <i>sakkaya-ditthi. </i>É
assim que o incontável número de <i>akusala kamma </i>do passado cometido em
existências e ciclos cósmicos passados que ainda não produziram seus resultados
acompanham aquele ser aonde quer que ele renasça. Esses <i>akusala kamma </i>passados
se assemelham a contas que são amarradas e mantidas juntas por um fio
resistente.<o:p></o:p></span></p>

<p class=Normal>Os seres, no entanto, que
claramente percebem a característica de <i>anatta, </i>e que se livraram de <i>sakkaya-ditthi</i>,
percebem o <i><a href="../glossario.php#rupa">rupakhanda</a> </i>e <i><a
href="../glossario.php#nama">namakhanda</a></i><span style="mso-spacerun:
yes">  </span>que surgem e cessam, até mesmo no decorrer de uma breve meditação,
como fenômenos distintos e não como parte de um continuum. O conceito de ‘meu <i>atta’</i>
que é igual ao fio, não está mais presente. Os <i><a
href="../glossario.php#khandha">khandas</a> </i>são percebidos como o cordão de
contas em que o fio foi removido. Eles claramente percebem que os <i>akusala
kamma </i>que foram cometidos no passado não são ‘pessoas’, ou ‘seres’ ou ‘eu’,
ou ‘meu <i>kamma,’</i> e são eles que surgem e cessam num instante. É por isso
que os <i>akusala kamma </i>desaparecem por completo assim que <i>sakkaya-ditthi
</i>desparece.<o:p></o:p></span></p>

<p class=Normal>Agora, deve ser observado
que apenas os <i>akusala kamma </i>desaparecem. Os <i><a
href="../glossario.php#kusala">kusala kamma</a> </i>do passado não desaparecem
através do mero desaparecimento de <i>sakkaya-ditthi</i>. É somente quando o
estágio de <i>arahatta magga</i> (NT.: caminho do arahant) é realizado, quando <i><a
href="../glossario.php#tanha">tanha</a> </i>é completamente erradicado, que os <i>kusala
kamma </i>também desaparecem por completo. <o:p></o:p></span></p>

<p class=Normal><b>O Mal de <i>Sakkaya-Ditthi</i></b></span></b><span
lang=PT-BR style='mso-ansi-language:PT-BR'><o:p></o:p></span></p>

<p class=Normal>Sakkaya-ditthi </span></i><span
lang=PT-BR style='mso-ansi-language:PT-BR'><span style="mso-spacerun:
yes"> </span>é um mal extremamente profundo e de grande alcance. <o:p></o:p></span></p>

<p class=Normal>Uma pessoa que cometa
matricídio e que por isso fica extremamente agitada e preocupada com a
possibilidade de com certeza renascer no inferno de <i>avici, </i>transforma o <i>kamma
</i>de matricídio em ‘<i>atta’</i> e fica muito angustiada pelos pensamentos
aos quais ela está firmemente apegada como, ‘Eu de fato agi de forma incorreta.
Eu de fato errei.’ Se esse ser compreender e realizar de forma completa a
característica de <i>anatta</i> <i>(anatta parinna)</i> e puder dessa forma
abandonar o apego a esses pensamentos como ‘Eu de fato errei,’ aquele <i>kamma </i>do
matricídio não mais terá o poder de produzir resultados no que diz respeito
àquele ser. Mas, os seres não descartam o seu apego a tais pensamentos.<o:p></o:p></span></p>

<p class=Normal>Embora, por assim dizer,
aquele <i>kamma </i>não deseje acompanhar aquele ser e não deseje produzir
resultados, ele é forçado ou coagido a fazer isso pelo fato de que o ser toma
posse desse <i>kamma </i>ao abrigar pensamentos como ‘É um <i>kamma </i>que
cometi. É o meu <i>kamma.’</i><span style="mso-spacerun: yes">  </span>Devido a
esse ato possessivo coercivo, aquele <i>kamma </i>é forçado a produzir os seus
resultados. Dessa forma, os seres mundanos que possuem <i>sakkaya-ditthi </i>estão
deludidos e perdidos nos seus caminhos.<o:p></o:p></span></p>

<p class=Normal>O mesmo ocorre com os
demais <i>akusala kamma. </i>É devido ao ato possessivo coercivo de <i>sakkaya-ditthi</i>
que os <i>akusala kamma </i>acompanham os seres através do <i>samsara,</i> onde
quer que eles renasçam e<span style="mso-spacerun: yes">  </span>produzem os
seus resultados.<o:p></o:p></span></p>

<p class=Normal>Os seres descobrem que não
são capazes de descartar os <i>akusala kamma </i><span style="mso-spacerun:
yes"> </span>mesmo quando<span style="mso-spacerun: yes">  </span>oprimidos
pelos seus resultados e por causa deles se encontrarem num processo de sofrer
grandes privações. Esses seres consideram esses <i>akusala kamma </i>como ‘<i>akusala
kamma </i>que eu cometi’, e dessa forma se apossam deles mesmo se estiverem
num<span style="mso-spacerun: yes">  </span>processo de sofrimento infernal
como conseqüência dos resultados produzidos pelos <i>kammas.</i> Como os seres
não conseguem descartar, abandonar, esses <i>akusala kamma, </i>esses <i>kammas
</i>não possuem outra opção que produzir os seus resultados. Esses <i>kammas </i>continuam
a produzir resultados tais que esses seres são incapazes de alcançar a sua
libertação de existências nos infernos. É nessa extensão que <i>sakkaya-ditthi </i>é
equivocado e produz um mal profundo.<o:p></o:p></span></p>

<p class=Normal>Da mesma forma, os seres
temem os perigos das doenças, envelhecimento e morte de maneira extremada. Mas,
mesmo abrigando tal medo eles se tornam apegados a incidentes passados de
doenças, envelhecimento e morte através de pensamentos tais como ‘Eu sofri
tantas vezes no passado por doenças, envelhecimento e morte’. Assim, eles se
vêem incapazes de abandonar e descartar até mesmo um<span style="mso-spacerun:
yes">  </span>fenômeno tão apavorante. E por que eles são incapazes de
abandoná-los e descartá-los, o fenômeno da doença, envelhecimento e morte
acompanham-nos contra a vontade deles e continuam a causar opressão. É assim
que o fenômeno da doença, envelhecimento e morte é obrigado a aparecer. É nessa
extensão que <i>sakkaya ditthi</i> é equivocado e produz um mal profundo.<o:p></o:p></span></p>

<p class=Normal>Também nesta existência,
quando perigos externos e internos são encontrados e os seres se tornam oprimidos
por enfermidades e indisposições, eles desenvolvem um apego por essas
enfermidades e indisposições através de pensamentos como, ‘Eu sinto dor. Eu me
sinto magoado. Eu me sinto oprimido por sensações de queimação,’ e assim se
apossam delas. Esse ato de posse é um ato de cativeiro que irá mais tarde
evitar que eles se livrem das enfermidades e indisposições. É devido à força
desse ato de cativeiro de <i>sakkaya-ditthi </i>que no ciclo de <i>samsara, </i>longo
e sem um início definido, os seres possuem essas enfermidades e indisposições
como seus companheiros inseparáveis até a presente data. É assim que <i>sakkaya-ditthi
</i>desenvolve o apego e se apossa até mesmo dessas enfermidades e
indisposições que oprimem de forma significativa os seres.<o:p></o:p></span></p>

<p class=Normal>Muito embora esses grandes
perigos e sofrimentos não queiram, por assim dizer, acompanhar aqueles seres,
eles são incapazes de evitá-lo e são obrigados a acompanhá-los de forma
contínua, de existência em existência, devido ao arrastão exercido por <i>sakkaya-ditthi.<o:p></o:p></i></span></p>

<p class=Normal>Em existências futuras
também, os apegos engendrados por<span style="mso-spacerun: yes"> 
</span>pensamentos tais como, ‘Experimentaremos enfermidades e indisposições,
encontraremos a velhice, encontraremos a morte,’ são ações de <i>sakkaya-ditthi</i>
que se apossa das eventualidades futuras de enfermidades, envelhecimento e
morte, a partir do momento presente, atando-as aos seres. Enquanto esse ato de
cativeiro não for destruído é certo que esses seres no futuro irão encontrar
essas eventualidades. É nessa extensão que <i>sakkaya-ditthi </i>é equivocado e
produz um mal profundo.<o:p></o:p></span></p>

<p class=Normal>Esta é uma breve descrição
de como <i>sakkaya-ditthi </i><span style="mso-spacerun: yes"> </span>é
equivocado e produz um mal profundo.<o:p></o:p></span></p>

<p class=Normal><b>Apego Superficial e
Profundo</b><o:p></o:p></span></b></p>

<p class=Normal>Os apegos de <i><a
href="../glossario.php#tanha">tanha</a> </i>e <i><a href="../glossario.php#mana">mana</a>
</i>não são apegos de <i><a href="../glossario.php#ditthi">ditthi</a>.</i> <i>Tanha
</i>desenvolve o apego por todos os fenômenos nas três esferas de existência
sob a forma de ‘É minha propriedade.’ <i>Mana </i>desenvolve o apego a eles sob
a forma de ‘Sou eu.’ No caso de seres que possuem <i>sakkaya-ditthi, tanha </i>e
<i>mana</i><span style="mso-spacerun: yes">  </span>seguem a liderança dada por
<i>sakkaya-ditthi; </i>no caso daqueles que ‘entraram na correnteza’, ‘que
retornarão uma vez’ e ‘que não retornam’ que se livraram de <i>sakkaya-ditthi,
tanha </i>e <i>mana<span style="mso-spacerun: yes">  </span></i>seguem <i>sañña
vipallasa </i>(distorções da percepção) e <a name=PVW><i>citta</i></a><i>
vipallasa </i>(distorções da mente). Os apegos produzidos por <i>sañña </i>e<i>
citta vipallasa </i>são superficiais. Os apegos produzidos por <i>sakkaya-ditthi
</i>são profundos.<o:p></o:p></span></p>

<p class=Normal>Aqui termina a descrição de
como os <i>akusala kamma </i>cessam totalmente com o desaparecimento de <i>sakkaya-ditthi.
</i><span style="mso-spacerun: yes">  </span><o:p></o:p></span></p>


