<p class=Tit1 align=center style='text-align:center'>Ayya Khema</Tit1>

<p class=Normal>Ayya Khema (1923-1997) foi a primeira mulher no Ocidente a se tornar uma monja Theravada. E como tal, ela serviu como modelo e inspiração para as mulheres de todas as tradições Budistas que buscam ressuscitar a prática do monasticismo feminino na era moderna. Embora a sua reputação como mestre esteja amplamente difundida, poucos conhecem os verdadeiramente incríveis detalhes da sua vida antes da sua ordenação monástica aos 56 anos de idade.<o:p></o:p></span></p>

<p class=Normal>E que vida ela teve. Ayya Khema, nascida Ilse Kussel em Berlim, Alemanha, cresceu numa  próspera família judia que se desintegrou com a ascensão do regime nazista em 1938. A história da sua fuga sozinha, juntamente com outras duzentas crianças, para a Escócia, e a sua jornada para reencontrar-se com a família em Xangai na China seriam o suficiente para preencher um bom livro de aventuras. Mas esse foi apenas o início da sua trajetória. Mais tarde as suas aventuras incluíram a sobrevivência à invasão japonesa da China, durante a qual ela juntamente com a  família foram internados num campo de prisioneiros de guerra Japonês e foi ali que o pai dela faleceu; a vida como uma dona de casa nos subúrbios de Los Angeles; uma viagem pelo Amazonas; estudos numa Universidade na Bolívia; a construção de uma estação geradora de energia no Paquistão e o estabelecimento da primeira fazenda com cultivo orgânico na Austrália.<o:p></o:p></span></p>

<p class=Normal>A sua prática Budista surgiu como resultado de uma busca espiritual que começou por volta dos quarenta anos de idade quando ela se encontrou com mestres espirituais na Índia. As suas experiências levaram-na por fim a se tornar uma monja Budista no Sri Lanka em 1979, quando recebeu o nome ‘Khema’ que quer dizer segurança e proteção (Ayya significa venerável).   Ela contribuiu para o estabelecimento do Wat Buddha-Dhamma, um monastério de florestas na tradição Theravada, próximo a Sydney, Austrália, em 1978. No Sri Lanka ela estabeleceu o International Buddhist Women's Centre em Colombo como um centro de treinamento para monjas Cingalesas e o Parappuduwa Nun's Island em Dodanduwa. Ela atuou como diretora espiritual do Buddha-Haus na Alemanha, estabelecido em 1989. Em Junho de 1997 o “Metta Vihara,” o primeiro monastério de florestas Budista na Alemanha foi inaugurado por ela.<o:p></o:p></span></p>
<p class=Normal>Em 1987 ela coordenou pela primeira vez na história Budista uma conferência internacional de monjas Budistas que resultou no estabelecimento da Sakyadhita, uma organização mundial Budista feminina. Em Maio de 1987 ela foi a primeira monja Budista a ser convidada para discursar nas Nações Unidas acerca do tema Budismo e a Paz Mundial.<o:p></o:p></span></p>

<p class=Normal>Ayya Khema escreveu vinte cinco livros sobre meditação e ensinamentos do Buda em Inglês e Alemão; os seus livros foram traduzidos em sete idiomas. Em 1988, o seu livro<i> "Being Nobody, Going Nowhere"</i> recebeu o Christmas Humphreys Memorial Award.<o:p></o:p></span></p>

<p class=Normal>Ayya Khema faleceu de câncer no dia 2 de Novembro de 1997 no Buddha-Haus, Alemanha.<o:p></o:p></span></p>

