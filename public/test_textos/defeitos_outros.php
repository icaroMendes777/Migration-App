<p class=Normal> ESSAS DUAS
ESTROFES do Dhammapada são de relevância universal e capazes<span
style="mso-spacerun: yes">  </span>de gerar insights significativos. Na
primeira estrofe, nossa tendência para esconder nossos defeitos é como o
subterfúgio de trapacear, porque essencialmente estamos sendo desonestos para
com nós mesmos. Reconhecer como somos na realidade é extremamente difícil,
especialmente em relação aos nossos defeitos, pois a nossa opinião sobre nós
mesmos é sempre tão fora do ponto, ela é ou muito alta, ou muito baixa. A
melhor maneira de obtermos um retrato claro e realista de nós mesmos é nos
observarmos com atenção plena. <o:p></o:p></span></p>

<p class=Normal>Não é difícil para nós notarmos os defeitos das outras
pessoas, pois eles freqüentemente nos irritam e nesse estado negativo ficamos
convencidos de que aquilo que pensamos é o certo e que por isso temos o direito
de julgar. E nós ficamos prontos para criticar, e ao fazer isso esquecemos que
o nosso pensamento está baseado<span style="mso-spacerun: yes">  </span>nas
nossas próprias opiniões, que não são completamente imparciais. Num certo
sentido, todas as nossas opiniões estão erradas, pois elas estão enraizadas na
ilusão do nosso ego: “eu tenho, eu quero, eu farei; eu acredito, eu sei, eu
penso.” No nível relativo, essas nossas opiniões podem retratar a verdade, mas
a verdade relativa não poderá nunca ser suficiente para nos satisfazer
completamente, pois no fim, ela pode expressar apenas a verdade de um ego
contra a de um outro ego.<span style="mso-spacerun: yes">  </span>Uma pessoa
acredita nisso, uma outra acredita naquilo; uma faz isso de um jeito, uma outra
faz isso exatamente do jeito oposto. A verdade construída em torno da noção de
um ego não pode ser absoluta e pura. Na melhor das hipóteses, ela reflete
preferências pessoais. A verdade relativa não pode ir além disso.</p>

<p class=Normal><span style="mso-spacerun: yes"> </span>Partindo do ponto de
vista da verdade absoluta, temos<span style="mso-spacerun: yes">  </span>um
quadro muito diferente. A partir dessa perspectiva, nós começamos a entender que
os defeitos que nos preocupam nos outros deveriam ser reconhecidos em nós
mesmos com o mesmo interesse. Os defeitos dos outros são um reflexo dos nossos
próprios, pois do contrário não seríamos capazes de reconhecê-los. Quando vemos
alguém com raiva ou exibindo-se com ostentação, nós reconhecemos esses defeitos
por já termos experienciado esses estados em nós mesmos. Nós sabemos como essas
reações emocionais surgem e como as sentimos. Do mesmo modo, diz-se que só um
Buda pode reconhecer um Buda, porque só<span style="mso-spacerun: yes"> 
</span>um iluminado conhece a iluminação.</p>



<p class=Normal> Quando nos
damos conta de que estamos criticando outras pessoas, deveríamos entender que
estamos no caminho errado. É improvável que a nossa crítica terá alguma
utilidade; pensando bem, quando foi que alguém mudou depois de ser duramente
criticado? Espalhar negativismo é sempre prejudicial, principalmente para nós
mesmos. É provável que uma outra pessoa fique irritada conosco e se reagirmos
com raiva e ressentimento, entraremos num círculo vicioso de mais negativismo
ainda, e possivelmente perderemos um amigo. <o:p></o:p></span></p>



<p class=Normal>  Portanto, a
crítica não ajuda – mas ficar ciente, sim. Se, por exemplo, notamos alguém
agindo sem atenção plena, a reação correta seria, “Eu me pergunto quão atento
estou neste momento?” E essa é a única resposta válida. Se observarmos algo
inábil no comportamento de uma outra pessoa e quisermos criticá-la, deveríamos
nos lembrar que a crítica é prejudicial para nós mesmos.<o:p></o:p></span></p>


<p class=Normal> Quando a
crítica repetida se tornar um hábito, nós teremos entalhado marcas de negativismo
dentro de nós mesmos. Provavelmente todos nós conhecemos alguém que faz da
crítica um hábito, e nós sabemos o quão desagradável é escutá-lo. Portanto,
devemos estar alertas para com as nossas críticas, e evitar que as outras
pessoas sofram com esse hábito desagradável. Deveríamos também estar
conscientes de que cada vez que criticamos, estamos gradualmente formando um
hábito.<o:p></o:p></span></p>


<p class=Normal> E se, ao
contrário, usarmos a oportunidade para observar o que está acontecendo com as
outras pessoas, poderemos usar o que notamos no comportamento delas como um
tipo de espelho de nós mesmos. E esse é um espelho precioso, porque embora ele
não nos proporcione uma vista dos nossos traços físicos, ele nos permite
começar realmente a difícil tarefa do auto-conhecimento. Essa tarefa é difícil
porque nós não só somos desprovidos de atenção, como preferimos essa situação –
nós preferimos não conhecer a verdade; estamos ansiosos por evitá-la por que
tememos que ela seja desagradável.<span style="mso-spacerun: yes">         
</span><o:p></o:p></span></p>


<p class=Normal> Dois dos
oito fenômenos mundanos estão envolvidos aqui: elogio e crítica.
Preocuparmo-nos em ganhar elogio e evitar a crítica é obviamente um pouco
absurdo, mas na verdade nós não questionamos isso.<span style="mso-spacerun:
yes">  </span></span>E além disso, essa preocupação é a responsável pela
nossa<span style="mso-spacerun: yes">  </span>relutância em entrar em qualquer
tipo de auto-análise: temos medo de encontrar coisas que poderiam fazer com que
tenhamos que aceitar a nossa culpa. Preferimos usar viseiras, evitando olhar
para dentro de nós mesmos de maneira abrangente.</p>


<p class=Normal>O medo da censura pode ser tratado usando a fórmula,
“Reconheça, não critique, mude.”<span style="mso-spacerun: yes">   </span>O
primeiro passo é se tornar consciente desse medo de ser repreendido, do
desacordo, da falta de apoio e apreciação.</p>

<p class=Normal>A raiz de todo medo é o medo da não existência. De modo
subliminar, ele está presente<span style="mso-spacerun: yes">  </span>em todos
nós e pode emergir repentinamente com o pânico, simplesmente por que nós não
queremos incorrer em censura. Ao mesmo tempo, estamos sempre prontos para
colocar a culpa nos outros, com a crença de que isso não irá nos prejudicar.
Mas estamos<span style="mso-spacerun: yes">  </span>enganados, pois ao dar
lugar para a negatividade, somos nós que sofremos.<span style="mso-spacerun:
yes">    </span></p>



<p class=Normal>O medo da censura é o mesmo que o nosso medo da morte, ou o
nosso medo como agente do nosso ego, da nossa auto-afirmação. No final, é o
medo de não estar mais aqui. É claro que quando prevemos uma acusação, não
tememos desaparecer naquele momento; tememos o desaparecimento da nossa
auto-estima que depende da apreciação dos outros.</p>



<p class=Normal>Obviamente, isso é loucura, mas mesmo assim a maioria das
pessoas está totalmente convencida dessa idéia, algumas ao ponto da obsessão,
de modo que elas ficam o tempo todo tentando agradar a todos. Mas como
esperamos alcançar isso?<span style="mso-spacerun: yes">  </span>Nós, em
primeiro lugar, sequer sabemos quais são os sentimentos e desejos das outras
pessoas.</p>

<p class=Normal>Embora não possamos fazer tudo de acordo com o que os outros
querem, podemos sempre tentar fazer o que é mais benéfico.<span
style="mso-spacerun: yes">    </span></p>


<p class=Normal>Que nós queremos aplauso e elogio, é um fato da vida. Nossas
ações são geradas com esse fim e se o nosso desejo é frustrado, somos tomados
pelo medo que impede a objetividade. Em outras palavras, nós tememos a
crítica.<span style="mso-spacerun: yes">  </span>Para nos livrarmos desse medo,
deveríamos começar tentando não ser tão críticos nós mesmos, compreendendo que
o que quer que façamos retornará como um bumerangue.</p>


<p class=Normal>Esse primeiro passo em direção ao insight do princípio de
causa e efeito – algo observável por toda parte no universo – é insuficiente
para superar todo o medo. O segundo<span style="mso-spacerun: yes"> 
</span>passo envolve a compreensão da natureza do medo. Na nossa busca por
afirmação por parte dos outros, nos tornamos escravos do nosso <span
style="mso-spacerun: yes"> </span>ambiente. E enquanto esse ambiente deixar de
corresponder às nossas expectativas ou de confirmar o quão maravilhosos,
inteligentes e bonitos nós somos, continuaremos desconfortáveis. Tal atitude
torna a vida imensamente difícil e obstrui nosso<span style="mso-spacerun:
yes">  </span>progresso em direção ao auto-conhecimento.</p>


<p class=Normal>Por outro lado, o auto-conhecimento honesto é essencial para
que sejamos capazes de abandonar as nossas obsessões, inclusive o nosso medo da
censura. Pois só podemos abandonar aquilo que tivermos reconhecido
completamente por nós mesmos, e é bem desnecessário transformar o nosso medo da
censura em medo do auto-conhecimento. O fato de sermos capazes de abandonar o
ego, depois de termos compreendido que isso não significa que iremos morrer,
significa que o egocentrismo não é mais a força dominante na nossa vida. As
coisas não precisam revolver em torno de como nós as vemos todo o tempo. Ao
invés disso, abrimos um espaço dentro de nós para aquilo que é universalmente
verdadeiro. Nós então compreendemos que, por que existem erros em cada aspecto
da existência condicionada, a perfeição não será encontrada em lugar nenhum.</p>


<p class=Normal>Considere o fato da impermanência: tudo que existe deve
desaparecer, e nada permanece o mesmo.<span style="mso-spacerun: yes"> 
</span>Se tentarmos nos agarrar a alguma experiência, ela escapará como areia
através dos nossos dedos. A falta de confiabilidade fundamental nas coisas
pode, é claro, se tornar uma ocasião para uma crítica, especialmente quando
outras pessoas nos deixaram na mão por não manter um compromisso ou não
completar um trabalho como combinado.</p>


<p class=Normal>Ninguém, sob forma nenhuma, criticaria uma estrela no céu
quando ela se torna<span style="mso-spacerun: yes">  </span>supernova e
desaparece – nós sabemos que não teria sentido demonstrar esse tipo de
reprovação já que isso acontece naturalmente.<span style="mso-spacerun: yes"> 
</span>Mas na realidade, isso é a verdadeira natureza de todas as coisas, e
igualmente não faz sentido se queixar sobre a falta de confiabilidade de tudo o
mais no universo. Todas as coisas condicionadas são imperfeitas.</p>



<p class=Normal>Essa é a razão pela qual vale a pena olharmo-nos sem medo e
ver o que é que nós não gostamos nas outras pessoas. Desgostamos da
negatividade delas?<span style="mso-spacerun: yes">  </span>Deveríamos nos
examinar em relação à negatividade. Desgostamos da busca constante de atenção
da parte delas?<span style="mso-spacerun: yes">  </span>É possível que nós
também tenhamos o mesmo desejo de ser o centro de atenção? Dessa maneira,
conseguiremos nos conhecer cada vez mais.</p>


<p class=Normal>Todos nós conhecemos o medo que surge repetidamente no
decorrer desse exercício: “Talvez eu não seja tão bom quanto eu pensava e se eu
não sou bom, outras pessoas irão me desaprovar .” Eu chamo isso de<span
style="mso-spacerun: yes">  </span>“pensamento de resultado direcionado”: nos
enrijecemos de medo diante dessa ameaça como diante de uma chicotada, e isso
poderá levar à dor física. Nós acreditamos que tudo tem que ser perfeito,
correto em tudo. Mas<span style="mso-spacerun: yes">  </span>que expectativa é
essa?<span style="mso-spacerun: yes">  </span>No universo tudo caminha do seu
próprio jeito continuamente. Os rios fluem e qualquer tentativa de pará-los
causaria uma enchente. A vida flui continuamente; quando cada dia termina, um
novo dia nasce. Por que não nos entregamos a esse fluir das coisas e paramos de
pensar em colocar numa direção específica todas as coisas que têm que ser
corrigidas?</p>


<p class=Normal>Isso se aplica tanto à nossa meditação quanto a tudo o mais.
Embora possamos estar sentados calmamente na nossa almofada, sem que ninguém
nos diga nada ou nos critique,<span style="mso-spacerun: yes">  </span>ainda
assim descobrimos que nós mesmos estamos colocando pressão e bloqueando a nossa
meditação. Se pensarmos que a nossa meditação tem que ser perfeita, seremos
incapazes de meditar, e só encontraremos ansiedade. Não tem sentido pretender
fazer tudo de modo perfeito; só podemos nos esforçar para fazer o melhor
possível. Estaremos em melhor situação se desistirmos do desejo de sermos
estimados. É claro que se alguém nos demonstra sua apreciação, gratidão e mesmo
contentamento pelo que fizemos, isso é bom, mas bom para eles.</p>


<p class=Normal>Deveríamos lembrar também que estamos mudando
constantemente. Nossos poderes e<span style="mso-spacerun: yes"> 
</span>capacidades podem ser vistos flutuando de um momento para outro. Isso
também se aplica na meditação. Algumas vezes a mente foca muito rapidamente; em
outras ocasiões<span style="mso-spacerun: yes">  </span>ela pode ter que se
livrar de tantos pensamentos que uma hora terá passado antes mesmo de termos
alcançado um grau de silêncio interior. Nós tendemos a atribuir essa
inabilidade ao nosso “ego” e assumí-la como nossa. Mas, por que será que
sentimos a necessidade de fazer isso? O que acontece realmente é que a mente
está constantemente mudando. </p>


<p class=Normal>Se pudermos ver como tudo muda em nós mesmos, será lógico
concluir que o mesmo acontece com todos os demais. Se uma pessoa se comporta de
modo indigno de elogio, deveríamos considerar que ela mudará, na esperança de
que seja<span style="mso-spacerun: yes">  </span>para melhor. Assim, ao
tornarmo-nos mais conscientes da impermanência, especialmente da impermanência
do mau comportamento, nós acharemos mais fácil abandonar o hábito de prestar
atenção obsessiva aos pequenos erros e de criticar. </p>


<p class=Normal>Como vimos, aquilo de que nos ressentimos mais nos outros
são aquelas características<span style="mso-spacerun: yes">  </span>que menos
gostamos em nós mesmos. Nós também vimos que se despendermos algum tempo, de
vez em quando, para investigar e compreender essas tendências, poderemos fazer
um esforço para superá-las. Entretanto, no decorrer desse processo estaremos
susceptíveis de fazer algumas críticas pesadas, porque ao mesmo tempo que o
comportamento que observamos possa se assemelhar com o nosso próprio
comportamento, as pessoas que criticamos podem não estar fazendo o esforço para
a purificação que nós fazemos. E essa atitude cria uma quantidade enorme de
atrito nos relacionamentos; pode não ser de forma explícita, mas mesmo assim,
estaremos guardando sentimentos de desaprovação e antipatia. Repetidamente
precisamos fazer um novo esforço para aceitar os outros e refrearmo-nos de
criticar. E isso é verdadeiro até em relação a nós mesmos. Não deveríamos
criticar e reclamar, mas ter sempre em mente a fórmula: “Reconheça, não
critique, mude.”</p>


<p class=Normal>A primeira parte - obter uma opinião clara sobre nós mesmos
- é o mais difícil. A segunda – não criticar, também não  é fácil, pois a mente
responde negativamente a qualquer sensação desagradável. Tudo que desgostamos
em nós mesmos – tudo que não podemos aceitar e que gostaríamos de mudar –
produz uma sensação desagradável e a auto-reprovação, e com isso podemos perder
a noção do caminho para o auto-conhecimento. </p>


<p class=Normal>É o insight da impermanência que facilita a busca do
auto-conhecimento. Quando se tornar claro que tudo que vemos está
desaparecendo, ficará muito mais fácil evitar o hábito de nos criticarmos.Tudo
que vem, vai, e nunca mais retorna, e nada que vem em seguida será exatamente o
mesmo, não importando o quão parecido possa ser. Investigando a impermanência
deste modo, começaremos a ser capazes de nos aceitar e de aceitar os outros de
bom grado.&nbsp;</p>


<p class=Normal>Reconhecer a verdadeira natureza daquilo que criticamos nos
outros, irá nos ajudar<span style="mso-spacerun: yes">  </span>a desenvolver
uma nova opinião a nosso respeito. Nós nos libertaremos daquilo que nos ofende,
não evitando as pessoas com esses defeitos, mas deixando de lado a necessidade
de fazer com que os outros sejam responsáveis por não serem do jeito que nós
achamos que eles deveriam ser.</p>



<p class=Normal>Nesse processo, podemos reconhecer tanto a impermanência,
como <i>dukkha</i>. Compreender que <i>dukkha</i> surge de nossas próprias reações negativas,
que inclui o medo da crítica, torna as coisas mais fáceis para que nos
refreemos de criticar os outros. Podemos ver que quase todos conhecem o medo
que deriva da falta de apoio e da falta de julgamento positivo, e que esse tipo
de dependência das pessoas é extremamente desagradável. </p>



<p class=Normal>Como podemos supor que as outras pessoas possam ter sempre
uma opinião correta sobre nós? Será que não acordamos para o fato de que
estamos todos presos na armadilha da ilusão, que torna impossível para nós
termos uma opinião verdadeiramente objetiva? A ilusão é que somos indivíduos
separados e que podemos ter unicamente contatos agradáveis, através dos meios
dos sentidos, se formos suficientemente espertos para arranjar as coisas da
maneira certa.</p>


<p class=Normal>Todos vivem com essa ilusão que lhes proporciona o desejo
pela existência e o medo da aniquilação. Como, então, é possível que outra
pessoa nos confirme a nossa existência? Todos os medos refletem esse medo da
aniquilação. O medo não está limitado ao medo relacionado com a nossa
existência física, mas se estende para a nossa existência emocional ou
auto-afirmação. Se nos tornarmos cientes desse medo, poderemos desenvolver uma
profunda empatia em relação às outras pessoas, pois toda a humanidade sente
esse desejo pela existência, que produz o <i>dukkha</i> mais severo.</p>


<p class=Normal>Esse medo profundamente enraizado é o obstáculo no caminho
para o contentamento perfeito, num nível humano básico, e uma vez que
comecemos<span style="mso-spacerun: yes">  </span>a entender essa correlação,
pararemos a busca da satisfação nos lugares errados. Ao invés disso, tentaremos
transcender as dificuldades da condição humana causadas pela ilusão do ego.
Mas, primeiro, temos de reconhecer que o medo do auto-questionamento, junto
com<span style="mso-spacerun: yes">   </span>o medo de ser criticado pelos
outros, e a correspondente compulsão para criticar, são motivados pela
necessidade de reforçar a nossa auto-afirmação. Ao condenarmos os outros,
estamos fazendo com que nos sintamos melhor. Chegaremos perto da verdade se
admitirmos que todos nós temos fraquezas.<span style="mso-spacerun:
yes">                 </span></p>


<p class=Normal>Esse reconhecimento nos leva um grande passo adiante em
direção ao insight quanto à fundamental insuficiência da existência, nesse
nível humano, condicionado. E só quando nos tornarmos cientes dessa
insuficiência é que experienciaremos uma urgência de deixar esse nível<span
style="mso-spacerun: yes">  </span>para trás – não fisicamente, é claro, mas no
que se refere a abandonar nossa ilusão do ego. Os problemas que temos que
superar não nos incomodarão mais e nós ganharemos o insight de quaisquer outros
problemas que continuem a nos causar dificuldades. Seremos capazes de ver que
ainda temos de transcender aqueles problemas, uma vez que alguns eventos ainda
podem nos incomodar. Se, por exemplo, ao ler nos depararmos com más notícias e
instantaneamente sentirmos negatividade brotando dentro de nós, poderemos
assumir que ainda não perdemos nosso desejo e a nossa raiva. Existe muito de
destruição no mundo, mas irritação e condenação mostram apenas que o ódio está
profundamente enraizado nas pessoas.</p>


<p class=Normal>Todos nós nascemos com seis raízes – três boas e três más –
e elas são a razão pela qual<span style="mso-spacerun: yes">  </span>não faz sentido
fazermos críticas, tanto a nós mesmos, como aos outros. A única resposta que
faz algum sentido é reconhecer essas raízes e nos comprometermos a encorajar as
boas raízes a florescer para que gradualmente atenuemos as prejudiciais.</p>


<p class=Normal>As raízes prejudiciais são, é claro, o desejo, a raiva, e a
delusão, (delusão no sentido de<span style="mso-spacerun: yes">  </span>ilusão
do ego). Mas os seus opostos também nos deveriam ser familiares. Se pudemos ver
as três raízes boas – generosidade, amor incondicional, e sabedoria – nas
outras pessoas, poderemos chegar à conclusão natural de que elas também estão
presentes dentro de nós. Na realidade, nós sabemos muito bem, exatamente
quando, onde e como praticar.<span style="mso-spacerun: yes">  </span>Palavras
e preceitos nunca são suficientes por eles mesmos, mas nós já temos sabedoria
dentro de nós, o bastante para perceber a verdade quando queremos ouví-la, e
saber onde ela pode ser encontrada.</p>



<p class=Normal style='margin-left:.75in'><i>Quem sempre busca defeitos nos outros,<br>
constantemente criticando,<br>
as suas impurezas se incrementam,<br>
estando distante da destruição das impurezas.</i>.</p>


<p class=Normal>Nessa estrofe, “destruição das impurezas” é um termo para
a perfeita purificação. Isso quer dizer que o desejo e a raiva não estão mais
presentes, e quando eles tiverem desaparecido a pessoa estará perto da completa
iluminação. Até lá, como essas palavras do Buda deixam claro, existe muito
trabalho interior a ser feito, pois enquanto criticarmos, não estaremos cientes
das nossas motivações e seremos incapazes de trabalhá-las.</p>


<p class=Normal>Essas motivações são principalmente as duas raízes do desejo
e da raiva. Ambas brotam da delusão, ou ignorância, da ilusão que nos leva a
crer que realmente existe “alguém.” No nível relativo é verdade que estamos
aqui sentados na nossa almofada de meditação, mas a verdade absoluta é
completamente diferente. E como vivemos de acordo com a verdade relativa, como
um “eu” que existe em relação a “você,” nós nos experienciamos como separados
dos outros e queremos proteger e construir paredes à nossa volta. Para realizar
isso, nós fazemos uso dessas motivações e a cada vez que formos negativos, elas
são fortalecidas .</p>



<p class=Normal>Essa é a razão pela qual é tão importante observarmos nossas
reações emocionais com atenção plena – estarmos conscientes delas
repetidamente, assim que elas ocorrerem, ainda que não possamos abandoná-las.
Uma vez que notemos essas reações, poderemos notar também quanta agitação elas
incitam e portanto, quão prejudiciais elas são para a calma que necessitamos
para a meditação. Na vida diária, não é fácil notar a diferença entre uma mente
calma e uma agitada; a meditação, a longo prazo, faz com que esse contraste
seja mais aparente. Notamos que nossas reações não consistem unicamente de
crítica; suas raízes podem ser encontradas no desejo, na aversão e no medo. </p>


<p class=Normal>De acordo com o Buda, nossos desejos crescem quando olhamos
para os defeitos dos outros e cedemos ao anseio por emoções negativas, pois
isso reforça a nossa separação, o que por sua vez leva a uma ilusão de ego
ainda mais entrincheirada. Por outro lado, nossos relacionamentos também podem
ajudar-nos a aprofundar o insight, se compreendermos que os outros estão
sujeitos às mesmas leis da impermanência, sofrimento e insatisfação como nós.
Na verdade, deveríamos considerar os relacionamentos com outras pessoas como
ocasiões para um aprendizado, e se os usarmos dessa maneira, nos beneficiaremos
de um sistema educacional de primeira classe.<span style="mso-spacerun: yes"> 
</span>De fato, podemos considerar nossa vida de modo geral como uma contínua
oportunidade para aprender. Todos os relacionamentos podem ser uma medida do
nosso treinamento no amor e compaixão e uma excelente oportunidade para
podermos nos conhecer.</p>


<p class=Normal>Se rejeitarmos ou condenarmos uma pessoa, estaremos agitados
por dentro. No momento em que abandonamos esse sentimento de censura, a paz
retorna. Abandonar não é fácil, mas existem muitos insights pequenos que podem
ajudar-nos nessa direção, por exemplo, o insight de que nós mesmos criamos essa
agitação e que isso nos prejudica. </p>


<p class=Normal>Se continuarmos refletindo sobre a impermanência e <i>dukkha</i>,
começaremos a entender que o universo todo está sujeito a eles. Tudo está em
constante processo de dissolução, de desaparecimento e de um novo surgimento. É
por causa desse movimento ininterrupto de todas as coisas que nada pode ser
inteiramente satisfatório. Uma vez que reconheçamos o fato da impermanência em
todas as coisas, não sofreremos mais por isso.<span style="mso-spacerun: yes"> 
</span>Somos, afinal, parte de uma comunidade de bilhões de pessoas, e cada uma
experienciando exatamente o mesmo fato da vida.</p>


<p class=Normal>Podemos aplicar os princípios gerais da impermanência e
<i>dukkha</i> a qualquer situação. Observar essas características em tudo que
colocarmos os nossos olhos é o próximo passo no caminho em direção ao
insight.<span style="mso-spacerun: yes">  </span>Nós então veremos que nada é
perfeitamente satisfatório, que tudo é impermanente. Nessa análise, nenhuma
exceção pode ser feita; tudo tem de ser incluído. Não podemos dizer, “eu tenho
a experiência de <i>dukkha</i>, mas aquela pessoa que me causou tanto <i>dukkha</i> é uma
imprestável.” Na verdade, ela experiencia tanto <i>dukkha</i> quanto nós. Assim,
gradualmente, desenvolveremos um sentimento de que o mundo é uma totalidade, e
que ele não consiste meramente de fenômenos individuais.</p>


<p class=Normal>Cada vez que reagimos com medo, a soma total de medo no
mundo é aumentada. Cada vez que guardamos negatividade dentro de nós,
desaprovação ou crítica, a soma total de negatividade é aumentada. Por outro
lado, se compreendermos a impermanência e <i>dukkha</i>, esse insight aprofundará a
soma total de sabedoria no mundo. Se virmos claramente que cada indivíduo
carrega a responsabilidade pela totalidade, estaremos mais preparados para
viver num nível onde não vejamos mais todas as coisas como entidades
separadas.<span style="mso-spacerun: yes">  </span></p>


<p class=Normal>&nbsp;Cada boa ação acrescenta bondade no mundo, porque<span
style="mso-spacerun: yes">  </span>nós somos o mundo. Nossas sensações,
pensamentos, palavras e ações são um componente do mundo. Com base nisso, é pura
falta de visão criticar; fazer isso é deixar de ver as características
fundamentais, ou marcas da existência que são: impermanente, insubstancial e
insatisfatória. Quanto mais meditarmos e quanto mais profundamente absorvermos
e refletirmos sobre as verdades universais do dhamma, mais fácil será para nós
aplicarmos atenção plena nessas marcas da existência e na vida diária.<span
style="mso-spacerun: yes">   </span></p>


<p class=Normal>No nível da verdade absoluta não existem entidades separadas
– tudo é manifestação. Mas no nível relativo cada um carrega a responsabilidade
pela manifestação do bem. O medo é uma característica que pode ter suas origens
no desejo de reter uma natureza essencialmente fixa e separada como indivíduos,
e no desejo de que a vida seja agradável o tempo todo. Ambos os desejos não são
realistas: não podemos ficar aqui<span style="mso-spacerun: yes">  </span>para
sempre e as coisas não podem ser agradáveis o tempo todo, assim, o medo surge
em relação a esses dois objetivos e bloqueia o nosso caminho. O medo pode ser
uma emoção muito poderosa. Diz-se que o medo da morte é pior que a morte. Do
mesmo modo, este tipo de emoção desabilita qualquer tentativa de sustentar um
insight real. Quase todo meditador experimentou o medo que pode surgir durante
a concentração, quando a asseveração do ego repentinamente entra em temporária
suspensão. </p>


<p class=Normal>Uma vez vencido esse medo, o próximo passo é compreender que
estivemos correndo atrás de algo impossível.<span style="mso-spacerun: yes"> 
</span>Então, o desejo, realmente sério, de transcender esse nível humano comum
de existência se desenvolverá. O medo que surge no curso desse processo precisa
ser abandonado, não uma vez, mas muitas vezes, sempre que formos atacados pelo
medo de que o nosso ego esteja sob ameaça. Essencialmente, é o mesmo medo de
quando estamos sendo criticados ou quando nos é negada a certeza de um ego, que
tanto desejamos.<span style="mso-spacerun: yes">  </span>Existem<span
style="mso-spacerun: yes">  </span>muitos nomes diferentes para o medo, mas
basicamente é o medo da não-existência.</p>


<p class=Normal>O antídoto mais efetivo contra a nossa tendência de
criticar, os nossos próprios erros e os dos outros, é testemunhar a verdade da
impermanência e <i>dukkha</i>. Não é suficiente<span style="mso-spacerun: yes"> 
</span>dizermos para nós mesmos, “eu não deveria criticar.” Nós provavelmente
sabemos disso há muito tempo. O problema é que nós somos freqüentemente
atraídos pelas coisas que não deveríamos ser. Com relação a isso, só uma
atitude engajada com o insight, o principal propósito da meditação, é que
poderá nos ajudar.</p>


<p class=Normal>O objetivo da meditação é fazer com que experienciemos a nós
mesmos mais profundamente, e essa é a razão pela qual ela deveria ser apoiada
pela contemplação e reflexão para aumentar a nossa autoconsciência. Que grau de
medo<span style="mso-spacerun: yes">  </span>nós guardamos dentro de nós?<span
style="mso-spacerun: yes">  </span>Quanto tememos a perda de nós mesmos?<span
style="mso-spacerun: yes">  </span>Esse tipo de inquisição nos leva mais
próximos da verdade. O problema crucial aqui não é se somos capazes de
abandonar o nosso medo imediatamente, mas se podemos ganhar novos insights
através desse nosso exame.</p>


<p class=Normal>Poderemos aprender muitas coisas com os defeitos dos outros.
Acima de tudo, poderemos chegar a conhecer muita coisa sobre nós mesmos. E
quando assim o fizermos, alcançaremos um sentimento de conexão, de
solidariedade para com os outros, como se eles fossem nossos irmãos e irmãs. Do
contrário, enquanto nos mantivermos separados e enfatizando nossas diferenças
pessoais, nossos apegos, nossos desejos e raiva terão a sua força aumentada.</p>



