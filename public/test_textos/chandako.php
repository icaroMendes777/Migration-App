<p class=Tit1 align=center style='text-align:center'>Ajaan Chandako </Tit1>

<p class=Normal>Ajaan Chandako (James Reynolds) nasceu em Minneapolis, Minnesota, em 1962. Ele se formou em 1984 com um BA em Religiões Orientais no Carleton College em  Minnesota. Além dos estudos ele foi membro de uma banda de blues e desenvolveu interesse pela meditação Zen. Depois da universidade ele viveu durante algum tempo num monastério Zen em Minnesota, trabalhou como cozinheiro num restaurante natural em Nova Yorque, viajou pela Europa, viajou os Estados Unidos de costa a costa de carona, estudou aikido na California, circundou o Hawai como mochileiro e passou um verão como guarda florestal nas montanhas Sierra Nevada. </p> 
<p class=Normal>Em 1987 ele deixou a sua casa na California e viajou para a Tailândia e o Tibet em busca de novas aventuras. Na Tailândia ele esteve praticando meditação vipassana durante um retiro de 3 meses. Na jornada pelo Tibet ele percorreu 3.500 milhas durante 6 meses. O relato dessa aventura foi publicado no livro <i>The Outer Path – Finding my Way in Tibet</i> com prefácio do Dalai Lama. Foi durante esse percurso pelo Tibet que ele acabou tomando a decisão de dedicar a sua vida ao Budismo, retornando então, em 1988, para a Tailândia. </p> 
<p class=Normal>Em 1990, com 27 anos de idade, ele foi ordenado como monge Budista na tradição das florestas de <a href="tradicao_de_florestas.php#Chah">Ajaan Chah</a>. Os primeiros cinco anos da vida monástica foram passados em treinamento no monastério Wat Pah Nanachat, que havia sido estabelecido por Ajaan Chah para monges estrangeiros. Depois disso, Ajaan Chandako passou a maior parte do seu tempo treinando com mestres de meditação em várias partes da Tailândia e dois anos com <a href="Brahmavamso.php">Ajaan Brahmavamso</a> em Perth, na Austrália. </p>
<p class=Normal>Ele é o autor de <i> A  Honed  and Heavy Axe, Samatha and Vipassana in Harmony.</i></p>
<p class=Normal>Mais recentemente ele tem ensinado na Austrália, Europa e Estados Unidos e na atualidade ele é o abade do Vimutti Buddhist Monastery  próximo a Auckland, Nova Zelândia. </p>

